            <font face="Verdana" size="1"><b>How important do you think is 
            CEO/MD succession planning for an organisation?</b><br>
            Succession planning of key management positions is a very important 
            topic in business, that touches the performance sustainability of 
            organisations. In fact, its relevance does not limit itself to 
            private and commercial organisations, but goes beyond to cover any 
            activity that depends on human resources. While organisations 
            generally address succession in the middle and junior ranks with a 
            lot of focus, the top layer tends to be taken with lesser urgency 
            due to the comfort that more senior position have less turnover.<br>
            In order to understand the importance of change at top of an 
            organisation, one must understand the implications of an MD/CEO 
            role. Organisations vary in the level of sophistication of their 
            internal operating systems and dependence on the CEO in their 
            day-to-day operations. In principle, a CEO’s job should not be to 
            involve at the detail and tactics level of the functioning, but 
            rather the strategic and leadership level. Yet, the strategic 
            functioning of organisations can only be based on the organisational 
            capabilities – including its culture. <br>
            <br>
            The role of the CEO is that of the full scope of the leadership of 
            the organisation, to clearly understand its operating environment 
            and its capabilities, to ensure its sustainability through ‘making 
            the most’ of what it has, ‘growing new business’ and ‘breaking new 
            grounds’ and deliver maximum value. This also encompasses bits of 
            the jobs of the Marketing manager, HR manager, Operations manager, 
            Finance manager, and so on, to provide strategic input into it.<br>
            <br>
            The issue of a sudden vacancy in a top position in an organisation 
            is probably a more difficult one to deal with – needless to say that 
            the organisational culture and the subtler aspects of business 
            policies and processes are difficult to transfer rapidly. Besides, 
            there is a very important element of knowing the organisational 
            history, in as far as its future is concerned. <br>
            <br>
            Success of the change process in such situations depends on both the 
            organisational culture as well as the incumbent.<br>
            <br>
            While unsuccessful change at the top should not normally render the 
            organisation still overnight, the organisational direction from the 
            top will influence the middle and long terms of its evolution, as 
            well as affect the short term, depending on the success of the 
            leadership migration. Therefore, a carefully planned succession will 
            spare the organisation potential long-term trauma, and a managed 
            interruption of top leadership input.<br>
            <br>
            <b>Is your company always prepared for a quick change at the top?</b><br>
            In Shell Oman, we conduct succession planning as a standard 
            organisational process, covering all aspects, including change at 
            the top. Shell Oman also has access to the Shell Group’s pool of 
            expertise and capabilities, which provides the company a reliable 
            fallback in cases of urgent need.<br>
            <br>
