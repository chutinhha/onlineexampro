<p align="justify"><font face="Verdana" size="4" color="#FF0000">Cruising along</font><font face="Verdana" size="1"><br>
<br>
<b>Alan E. Durante, Executive Director &amp; President (Automotive Sector), Mahindra 
&amp; Mahindra Ltd (M&amp;M), was in town last month to launch the Mahindra Scorpio, the 
latest sensation on Oman roads under the banner of Towell Auto Centre LLC. 
Excerpts from an exclusive Face2Face with Vicky Kapur</b><br>
<br>
<b><img border="0" src="sru.jpg" align="right" width="200" height="199">Welcome to Oman, Alan. How has your foray into the Middle East market gone so 
far?</b><br>
Thank you. It has been pretty smooth sailing so far. Over the last four to five 
months, we have launched the Mahindra Scorpio vehicle in Kuwait, from where we 
moved to Qatar, then the UAE (Dubai), and our latest foray is now in Oman. We 
have as well zeroed in on Yemen and Bahrain, and the next trip during the next 
month will be for the launch in Yemen where, without even launching the vehicle, 
we have an order for 26 vehicles already! <br>
<br>
<b>Why did you choose to launch the Scorpio first in Kuwait?</b><br>
We’ve had a relationship with (the dealer in) Kuwait, built over the last 10-12 
years, and the distributor there was extremely bullish as far as the Mahindra 
vehicle went. He virtually kept advance money with us, and said ‘‘I’d liked to 
be assured that the first vehicle to be launched in the Gulf will be at my 
dealership.’’ It’s more towards (honouring that commitment) that we kept the 
‘glamour’ of Dubai on the backburner and first launched the Scorpio in Kuwait. 
Of course, we did follow up within two months into the UAE market. <br>
<br>
<b>Are you not looking at the Suadi market?</b><br>
Saudi is a huge market, of course, and we would definitely like to get into it 
at some stage, but we decided to take on markets in line with we could chew – 
i.e., we have to train the dealers for service set-up, we have to ensure that 
there are enough spare parts in the system etc. The fastest way to lose a market 
is by not having a support infrastructure in place. So, we’re going about it 
systematically and in an organised way.<br>
<br>
<b>What training does your partner have to undergo before you launch the vehicle 
in a new market?</b><br>
We got the mechanics from WJ Towell to come to India, spend a month with us 
receiving training in all facets of engine and transmission assembly, 
fault-finding in the vehicle, paint-work, and also suspension. Vehicles are 
driven much faster here as compared to India, and thus are more prone to 
accidents. We have to ensure that the mechanics are in a position to repair the 
vehicles and get them back into a roadworthy condition.<br>
<br>
<b>Why did you choose WJ Towell as your a partner in Oman?</b><br>
WJ Towell has the one of the best dealerships in the Sultanate. A joint venture 
is like a marriage – you zero-in on somebody, and if it vibes well, then you go 
ahead and strike a deal. Frankly, we’ve been talking to Towell for the last 
five-six years, but to be honest, we did not have an exportable vehicle in our 
portfolio at that stage to meet the expectations of Gulf customers. We had great 
diesel vehicles, but that wasn’t a preferred vehicle for the region…<br>
<br>
<b>What are your expectations from the Oman market?</b><br>
We hope to gain a foothold for the Mahindra brand name in the Oman market. We 
hope to get, in terms of numbers, about 30-35 a month or roughly about 400-500 a 
year as a starting point in the first year. <br>
<br>
<b>What percentage of the total manufactured vehicles do you export outside 
India, you home base?</b><br>
We have a long-term strategy of selling 25 per cent of what we produce in India, 
abroad. Today, we are at 8-10 per cent. But we have really made a very strong 
foray only in the last six-eight months. The Scorpio has been homologated to 
meet the specs of Europe and the Gulf only over the last six months. This is a 
long-term plan, one that every company would like to have to take care of the 
downturns in the local industry due to natural or political reasons. <br>
<br>
<b>With the automobile market getting increasingly crowded, and the cross over 
vehicle now becoming a norm, where does the Scorpio compete?</b><br>
Our USP is really making sturdy, rugged vehicles at a very affordable price. 
Added to that would be its looks – the Scorpio and the Bolero are, in today’s 
parlance, sexy-looking vehicles that appeal to the young and have a lot of 
space, are rugged, and are priced extremely attractively. <br>
<br>
<b>The Scorpio doesn’t come with airbags as of now – are you looking at 
developing a version with the same for the Middle East market?</b><br>
We will not be enhancing safety features only for the Middle East market – we 
are currently developing airbags for the whole of Western Europe as well as the 
Gulf, and it’s on the card over the next six-eight months. However, the vehicle 
even without the airbags is extremely sturdy and safe. It has already met the 
crash-test requirements in the UK. <br>
<br>
<b>Why doesn’t M&amp;M get into the passenger car segment?</b><br>
We’ve taken a conscious decision as a company (of not competing in the passanger 
car segament). We firmly believe that our core competence and expertise lies in 
manufacturing and selling SUVs and MUVs (Sports Utility vehicles and 
Multi-Utility Vehicles). We believe we have a good brand name for products in 
this segment. Moreover, in the passenger car segment, the margins are 
wafer-thin, you have to come out with new models every couple of years – and 
that requires a lot of capital expenditure. However, this doesn’t preclude us 
from tie-ups. <br>
<br>
<img border="0" src="sru1.jpg" align="right" width="150" height="105">For instance, we have a tie-up with Renault to make passenger cars in India. 
Renault wants to use India as a big sourcing base for their Logan car and for 
sourcing components for their entire range of cars. As a next step, Nissan is 
also expected to look at India as a base for sourcing components. Renault has a 
left-hand drive Logan, and wants Mahindra to develop the right-hand version of 
the car. I.e., a whole lot of Mahindra’s engineering services are going to be 
used. That doesn’t mean that we are getting into manufacturing passenger cars – 
it’s just that our engineering expertise are being used for developing Renault’s 
right-hand drive Logan. <br>
<br>
Moreover, our engineering rates per hour are perhaps one of the lowest in the 
world and, as a consequence, we could see a lot of engineering operations being 
outsourced to us. Besides the engineering relationship, we are going to look at 
the Renault dealerships for export of our Scorpios in Eastern Europe and Africa, 
where their numbers may not be very high and we can compliment the dealerships 
with our Boleros and Scorpios. So, there are a lot of synergies that can evolve 
with time.</font><font face="Marlett" style="font-size: 5pt">g</font></p>

