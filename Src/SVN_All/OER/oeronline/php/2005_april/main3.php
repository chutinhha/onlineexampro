            from a high of $70 (RO26.92). Across the world, millions have been 
            wiped out from stock market capitalisation as investors run for 
            cover fearing a full-blown recession.<br>
            <br>
            <b>What went wrong? </b><br>
            After all, broad economic fundamentals, until recently, had not 
            changed that much. The UK and Europe tag along with reasonable 
            certainty while South East Asia (with the exception of Japan) has 
            recovered pretty well from their difficulties of a few years ago. 
            The main concern of course remains the turnaround of the American 
            economy, which if it deteriorates any further, might infect other 
            economies. <br>
            <br>
            Caught in the euphoria of New Economy and dot.com frenzy, investors 
            lost sight of basic investment fundamentals as they searched for new 
            untested valuation models to justify the premium prices. Market 
            share eyeball hits, intellectual property became the new valuation 
            tools. Due diligence decreased in importance as irrational 
            exuberance took over. <br>
            <br>
            Fuelled by high stock prices, acquisitions became easy and high 
            premium continued to be paid regardless of traditional rational 
            principles. Cisco Systems alone bought more than 70 companies since 
            1993. <br>
            <br>
            Indeed, the fact that some companies were not making a profit, and 
            gave no indication of when they were likely to break even (never 
            mind to become profitable) did not seem to matter any more. Torrents 
            of money pumped in by equity investors and venture capitalists kept 
            driving valuations up. And again, everyone seemed to miss the point 
            (i.e., you cannot create value by not making money and by 
            continually bleeding cash). <br>
            <br>
            The entrepreneurial spirit took over. MBA students (particularly in 
            the US) became less interested in portfolio management, 
            globalisation and organisational behaviour, but became restless to 
            start their own company and look for an initial public offering 
            (IPO). Venture capitalists could not keep pace with all the 
            unsolicited proposals hitting their desks. <br>
            <br>
            Warren Buffet, the legendary US investor, captured the mood very 
            well in his annual address to shareholders of Berkshire Hathaway, 
            his investment company: ‘‘Overstaying the festivities…will 
            eventually bring on pumpkins and mice…but they nevertheless hate to 
            miss a single minute of what is one helluva party’’. <br>
            <br>
            As far back as 1999, he refused to invest a single penny into high 
            tech, dot.com wannabes. His reasoning was simple: ‘‘We don’t invest 
            in any business that we do not understand. And we do not understand 
            any of these businesses’’. <br>
            <br>
            The Financial Times ahead of Lastminute.com’s flotation issued an 
            Investors Beware statement. Despite having some very hard working 
            and ingenious people with a good idea, this was not enough. Using 
            Porter’s famous Five Forces Model indicated high levels of risk on 
            threat of new entrants, bargaining power of suppliers and customers, 
            substitution and competition. The basic model was a high risk one. 
            This did not seem to deter investors as the issue was well 
            oversubscribed. <br>
            <br>
            Sublime naivety was once more illustrated by the retailer Boo.com, 
            one of the early casualties, who never made a profit, had lots of 
            fun until the cash completely ran out. Anybody involved there surely 
            should have been spotted that a call centre in Carnaby Street London 
            at premium rentals, was not clever. <br>
            <br>
            In summary, greed, dot.com fever and the promise of quick and easy 
            riches elbowed out well-proven investment principles. <br>
            <br>
            The last words should perhaps belong to Mr Buffett who, preferring 
            known cash flows from predictable businesses, borrowed a dictum from 
            his grandson: ‘‘A girl in a convertible is worth five in the phone 
            book’’.<br>
