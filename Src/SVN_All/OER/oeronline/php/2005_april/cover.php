                    Cover Story<br>
            <br>
                    </font><font face="Verdana" size="4" color="#FF0000">
            Succession Planning</font></p>
            <p align="justify" dir="ltr"><b><font face="Verdana" size="1">
            <font color="#004000">Does your successor fit the big picture?</font><br>
            Succession planning—the preparation to replace one leader with 
            another— is one of the most difficult challenges organisations face 
            oday. Few events in the life of an organisation are as critical, 
            visible, or stressful as when the leader leaves. The eyes of every 
            member, employee, customer, supplier, and stakeholder are focused on 
            the outgoing executive director or CEO. How such an exit is managed 
            reveals the character and effectiveness of that leader and 
            association. OER provides an exhaustive insight into the art and 
            science of succession planning</font></b></p>
            <p align="justify">
            <font face="Verdana" style="font-size: 14pt">
            <b>
            <img border="0" src="cover1.jpg" align="left" hspace="5" width="159" height="159" vspace="2"></b></font><b><font face="Verdana" size="3">I</font></b><font face="Verdana" size="1">n 
            recent months, a number of prominent companies around the world have 
            announced changes at the top. The reasons have ranged from the 
            unfortunate death of McDonald’s CEO James Cantalupo to the 
            un-ceremonial exit of Boeing CEO Harry Stonecipher over ‘an improper 
            relationship with a female executive’. Then there was also Sony, 
            which staged a sort of surprise by announcing the replacement of 
            native Nobuyuki Idei with foreigner Sir Howard Stringer. The most 
            recent announcement, of course, is that of former Aer Lingus boss 
            Willie Walsh taking over the pilot’s seat from British Airways’ Rod 
            Eddington after he retires in September this year.<br>
            <br>
            Of course, there are numerous reasons for a change at the top of the 
            organisational stepladder – a new manager brought in from the 
            outside; a key retirement; an executive waiting in the wings who 
            finally gets his or her chance; the splitting of the chairman/CEO 
            role into two separate positions; the departure of unsuccessful 
            contenders… Whatever the reasons for a transition, it offers a brief 
            window to create sustained organisational change. Beyond their 
            obvious effects on individual careers, such changes are also 
            opportunities—often not fully exploited—to alter the work culture of 
            the organisation. Never more so than when the change takes place at 
            the very top with the appearance of a new CEO. These ‘appearances’ 
            are becoming increasingly common as more industries face 
            discontinuities and more stakeholders assert their rights.<br>
            <br>
            Succession planning has implications for an entire organisation. It 
            is part of workforce planning, good management capability training, 
            and performance monitoring. It is also connected to career 
            development planning for individuals and other human resource 
            issues. This leads to future sustainability of an organisation, its 
            systems, structures, processes and the underpinning of 
            organisational culture.<br>
            <br>
            In the examples of corporate transitions cited earlier, all but one 
            company have a common thread – every one of them had a firmed up 
            succession plan in place. Sir Howard, earlier the head of Sony’s 
            successful Entertainment Business Group, was handpicked to succeed 
            him by the outgoing Noboyuki Idei, who will still be part of the 
            company as chief corporate advisor of Sony Corporation as of June 
            22, 2005, when the new management headed by Stringer takes formal 
            charge. ‘‘Sir Howard, an executive admired both within the Sony 
            Group and in the wider business community, fully understands Sony’s 
            longstanding spirit of freedom and open-mindedness,’’ said Idei 
            while announcing the transition at the world’s largest producer of 
            consumer electronics. <br>
            <br>
            On April 19, 2004, Charles Bell, who had been president and chief 
            operating officer of McDonald’s since January 1, 2003, succeeded 
            chairman and CEO James Cantalupo, who died unexpectedly hours 
            earlier of a heart attack. As Cantalupo’s deputy, Bell had helped 
            oversee a surprisingly swift turnaround of McDonald’s operations in 
            the US. In fact, in promoting Bell to president and COO, Cantalupo 
            had signalled that he was prepping Bell to take his place when he 
            planned to go back into retirement. On April 19, the McDonald’s 
            board convened in Orlando at 6:45 a.m., quickly endorsing the 
            outfit’s succession plan, elevating Bell, and leaving the rest of 
            the top management intact. Cantalupo had been scheduled to give the 
            keynote speech to McDonald’s owner/operators at 9:30 a.m. Instead, 
            Bell spoke at the convention at the end of the day, receiving a 
            standing ovation.<br>
            <br>
            British Airways last month announced the appointment of Willie Walsh 
            as its new CEO. Previously CEO of Aer Lingus, Walsh begins as chief 
            executive designate on May 3, 2005. Current CEO Rod Eddington will 
            retire at the end of September at which time Walsh assumes his new 
            position. Martin Broughton, the airline’s chairman, said: ‘‘Willie 
            has an outstanding reputation in the airline industry around the 
            world for the way in which he transformed the fortunes of Aer Lingus 
            from a high-cost under-performing carrier into a successful and 
            profitable entity.’’<br>
            <br>
            That leaves Boeing, where its board of directors asked for and 
            received the resignation of president and CEO Harry Stonecipher 
            earlier last month. Concurrently, the board appointed CFO James A. 
            Bell, 56, as president and CEO on an ‘interim’ basis, with board 
            chairman Lew Platt assuming an expanded role in his capacity as 
            non-executive chairman. Although Bell is a 32-year veteran of the 
            company who has served as Boeing CFO and as a member of the 
            company’s executive council since November 2003, he was neither 
            groomed to be the aircraft-maker’s CEO, nor is expected to be one 
            for more than a few months.<br>
            <br>
            Although Boeing’s board claims to have a robust succession planning 
            process in place, it announced that it was now ‘accelerating’ the 
            search for a permanent CEO, a position for which candidates both 
            inside and outside the company are being considered. Interestingly, 
            James Bell, the man the company’s board has entrusted with piloting 
            the company in the interim, is not among the candidates.<br>
            <br>
            <b>Nothing succeeds like succession</b><br>
            The fact that the Boeing board had not firmed up a succession plan 
            for Harry Stonecipher, who had intended retiring in any case in May 
            2006—and that Stonecipher himself had not designated and groomed a 
            successor—exposes a weakness in many companies’ strategic thinking. 
            To be fair to Boeing’s board, the exit of a Stonecipher, who was in 
            fact called back from retirement a couple of years back, was indeed 
            sudden. <br>
            <br>
            But that is exactly where a defined and definite succession planning 
            programme scores over thirteenth-hour headhunting. Had the 
            aircraft-maker’s board identified and announced a successor to the 
            outcast CEO in advance, they wouldn’t have to gamble on the 
            abilities of the company’s CFO to run the show in the interim – as 
            well as avoid the HR dilemma it is bound to face once a new CEO is 
            installed and Bell (ex-CFO or ex-CEO?) has to report to him. <br>
            <br>
            The concept of succession planning has become an important part of 
            many companies’ strategic planning – but not in all companies. Too 
            many think of succession planning as having application only in 
            family-owned companies or in large conglomerates. In fact, 
            succession planning should be a part of every company’s strategic 
            plan – the board/senior management’s collective vision of where the 
            company will be going in the future. <br>
            <br>
            The reasons for this approach are fairly obvious: If there is no 
            succession planning process, how will the company develop and 
            nurture its human capital? How will it assure a continuing sequence 
            of qualified people to move up and take over when the current 
            generation of managers and key people retire or move on? How will 
            the corporate boardroom be able to plan for the future of the 
            company without some assurance that the key posts will be filled 
            with people able to carry on and excel? Succession planning is much 
            more important than the time many companies devote to it would 
            indicate.<br>
            <br>
            So what really is involved in succession planning? Succession 
            planning is a part of the process of preparing for the future of a 
            company. Does this mean one should only plan a succession path for a 
            CEO? Corporate analysts suggest that virtually every key position 
            and key person in an organisation is a candidate for a succession 
            plan. The important impact is that it is virtually impossible to 
            successfully promote someone unless there is a trained person to 
            take over the position being vacated. To effectively implement a 
            succession plan, a director on the board of a company needs to 
            include/consider a number of elements:</font></p>
            <ul>
              <li dir="ltr">
            <p align="justify" dir="ltr"><font face="Verdana" size="1">
            What is the long-term direction of your company? Do you have an 
            effective strategic plan guiding your course and direction?</font></p>
              </li>
              <li dir="ltr">
            <p align="justify" dir="ltr"><font face="Verdana" size="1">
            What are the key areas that require continuity and development of 
            the people resources within your company?</font></p>
              </li>
              <li dir="ltr">
            <p align="justify" dir="ltr"><font face="Verdana" size="1">
            Who are the key people you want to develop and nurture for the 
            future?</font></p>
              </li>
              <li dir="ltr">
            <p align="justify" dir="ltr"><font face="Verdana" size="1">
            How does the concept of succession planning fit into your 
            strategies? Are you concentrating your efforts in the areas where 
            the returns will be highest?</font></p>
              </li>
              <li dir="ltr">
            <p align="justify" dir="ltr"><font face="Verdana" size="1">
            What are the career paths that your most talented people should be 
            following? Is each path customised to fit the abilities and talents 
            of the people involved?</font></p>
              </li>
            </ul>
            <p align="justify" dir="ltr"><font face="Verdana" size="1">
            <img border="0" src="cover2.jpg" align="right" width="200" height="115">The point here is to be sure that a company doesn’t do this by rote: 
            a plan that is not dynamic, that does not include the consideration 
            of the individuals involved, is not usually as effective as one that 
            is tailored to each individual’s needs and capabilities. Should one 
            wait for openings to appear before promoting someone, or should one 
            make opportunities for each individual as they grow and mature, so 
            that one can keep them challenged and stimulated, and not lose them 
            to other, possibly faster moving companies? An organisation’s plan 
            should be proactive, with people moving into different areas for 
            experience and training before they are needed in critical 
            positions, rather than reactive – waiting for openings to occur, 
            then scurrying around to find an appropriate candidate at the last 
            second.<br>
            <br>
            What strategies should a board be considering for its company’s 
            succession planning process? First, realise that one size doesn’t 
            fit all. There are different approaches that may be used, depending 
            on the situation in each company. In some cases, a company may have 
            to move some people along quickly, in order to expose them to a 
            broad range of experiences, and possibly to fill vacancies. In 
            others, a deeper involvement in selected departments or disciplines 
            may be indicated. Some of this will depend on the culture and 
            processes of the company. In yet other cases, decisions about the 
            process will depend on the individual’s capabilities and 
            competencies, and the structure and operations of the company. <br>
            <br>
            In virtually all situations, the senior management’s ability to 
            educate and promote will depend on the capabilities and strengths of 
            the people who currently occupy the key positions, and where they 
            will be going in the future – what are they being groomed for? It 
            may not be vital to have a succession plan for every position in the 
            company, but certainly there are some key areas of responsibility 
            that must be considered. These will vary by company and industry, 
            but as a part of a strategic planning process, one important 
            strategic issue should be the need for succession planning for 
            certain, defined key positions. This issue should be revisited at 
            least once a year, and more often if circumstances dictate.<br>
            <br>
            <b>Planning pros &amp; pitfalls</b><br>
            The advantages of succession planning include:</font></p>
            <ul>
              <li dir="ltr">
            <p align="justify" dir="ltr"><font face="Verdana" size="1">
            An ongoing supply of well trained, broadly experienced, 
            well-motivated people who are ready and able to step into key 
            positions as needed</font></p>
              </li>
              <li dir="ltr">
            <p align="justify" dir="ltr"><font face="Verdana" size="1">
            A cadre of desirable candidates who are being integrated into the 
            company with positive goals established for them individually</font></p>
              </li>
              <li dir="ltr">
            <p align="justify" dir="ltr"><font face="Verdana" size="1">
            A flow of these capable people through various departments with the 
            goals of educating them into the culture and processes of the 
            company</font></p>
              </li>
              <li dir="ltr">
            <p align="justify" dir="ltr"><font face="Verdana" size="1">
            Alignment of the future needs of the company with the availability 
            of appropriate resources within the company</font></p>
              </li>
              <li dir="ltr">
            <p align="justify" dir="ltr"><font face="Verdana" size="1">
            Positive goals for key personnel, which will help keep them with the 
            company and will help assure the continuing supply of capable 
            successors for each of the important positions included in the 
            succession plan</font></p>
              </li>
              <li dir="ltr">
            <p align="justify" dir="ltr"><font face="Verdana" size="1">
            Defined career paths, which will help the company recruit and retain 
            better people</font></p>
              </li>
              <li dir="ltr">
            <p align="justify" dir="ltr"><font face="Verdana" size="1">
            Very likely, the continuous input of ideas to improve the internal 
            processes and procedures of the company, as well as the 
            opportunities to improve the offerings and services of the company 
            in the marketplace</font></p>
              </li>
            </ul>
            <p align="justify" dir="ltr"><font face="Verdana" size="1">
            Recognise that all companies do not have to follow the same path 
            either in the overall situation or even for each individual. Each 
            situation should be analysed and optimised in terms of the company’s 
            and the individual’s needs. In addition, there should be enough time 
            allowed to groom the successors. They should not be expected to 
            learn the jobs/responsibilities overnight. Time is the gift that 
            good planning can bestow on the process. <br>
            <br>
            Also, the successor and potential back-ups should be designated 
            early in the process. Not only will this help the individuals 
            involved, it will clarify the communication and help eliminate 
            disappointment and possible departures of key candidates if they 
            become disillusioned because they have not been informed they are 
            being groomed for higher positions or if they feel they are not 
            moving upward rapidly enough.<br>
            <br>
            Possible pitfalls of succession planning include:</font></p>
            <ul>
              <li dir="ltr">
            <p align="justify" dir="ltr"><font face="Verdana" size="1">
            Lack of a formal written plan for each key person or position. A 
            rigid, inflexible plan NOT tailored to the needs and abilities of 
            the personnel involved</font></p>
              </li>
              <li dir="ltr">
            <p align="justify" dir="ltr"><font face="Verdana" size="1">
            Too long a wait for real movement/promotion, potentially resulting 
            in the best people leaving due to apparent inertia in the system</font></p>
              </li>
              <li dir="ltr">
            <p align="justify" dir="ltr"><font face="Verdana" size="1">
            Too superficial an approach, with the corresponding lack of real 
            understanding of the procedures, processes and requirements of each 
            area the individual is exposed to during the process</font></p>
              </li>
              <li dir="ltr">
            <p align="justify" dir="ltr"><font face="Verdana" size="1">
            Selection of unqualified or unmotivated people for inclusion in the 
            succession plan. Quality of the individuals selected is paramount to 
            the success of the process.
            </font></p>
              </li>
              <li dir="ltr">
            <p align="justify" dir="ltr"><font face="Verdana" size="1">
            Resistance from above to the grooming of strong candidates.</font></p>
              </li>
            </ul>
            <p align="justify" dir="ltr"><font face="Verdana" size="1">
            What are the benefits of good succession planning? With a 
            well-thought-out succession planning process, a company can earn a 
            number of clear pluses:</font></p>
            <ul>
              <li dir="ltr">
            <p align="justify" dir="ltr"><font face="Verdana" size="1">
            A well-trained, involved and potentially deep stream of capable 
            people who are well versed in the breadth and depth of the company.</font></p>
              </li>
              <li dir="ltr">
            <p align="justify" dir="ltr"><font face="Verdana" size="1">
            A continuous stream of people who are constantly reviewing, 
            questioning and refining procedures and processes, helping to 
            improve the quality inside the company, as well as improving the 
            offerings of the company out in the market place.</font></p>
              </li>
              <li dir="ltr">
            <p align="justify" dir="ltr"><font face="Verdana" size="1">
            An increasing reputation as a good, challenging, stimulating place 
            to work, which could result in the company’s ability to hire better 
            people.</font></p>
              </li>
            </ul>
            <p align="justify" dir="ltr"><font face="Verdana" size="1">
            <b>Succession by strategy</b><br>
            As in the deployment and utilisation of any strategic resource, the 
            development of a company’s key people must be considered as the top 
            management plans for the future of a company. It is certainly worth 
            considerable time and effort to discuss the company’s needs and 
            current capabilities thoroughly, as a strategic issue, and possibly 
            as a part of internal development strategy. If a company does not 
            have a formal procedure for succession planning, the board or top 
            management must mandate the development and installation of a 
            process that fits the needs and preferences of the company. <br>
            Some of the questions to be answered while firming up a succession 
            planning roadmap include: What does the company need to do to get 
            the best from its people, to position and educate them so they may 
            best contribute to the company’s and their long-term success, and to 
            assure a flow of competent, well-educated and experienced people for 
            the company’s future?<br>
            <br>
            How long should succession planning take? Realistically, succession 
            planning is never finished. On a regular basis, each company must 
            look at its needs and resources to determine where it needs to have 
            successors in place or in the process of learning the requisite 
            disciplines. Each company needs to determine how long a candidate 
            should be involved or exposed to the training needed. Each 
            individual should have a concisely determined path towards the goal 
            set for him or her. That path may be changed as needed and as events 
            determine; so, monitoring and updating should be a part of every 
            succession plan.<br>
            <br>
            Over what time period should one plan? To be realistic, succession 
            must be planned years in advance of expected needs. To properly 
            train a successor, the firm needs sufficient time to expose the 
            people to the full spectrum of opportunities within the firm, as 
            well as any desired or required outside education/experience 
            expected. For example, if someone is expected to be a general 
            manager, the number of departments, the types and ranges of 
            technologies and processes, and the level of knowledge about the 
            company procedures and policies, markets and customers, suppliers, 
            employees, contractors etc. will determine the time and depth of 
            involvement. Additional factors, such as past experience and current 
            knowledge that the individual brings to the process, will also 
            affect the succession time frame. Skilfully done, succession 
            planning will bring the peace of mind that senior management should 
            have, based on the understanding and expectations of its future 
            leadership.<br>
            <br>
            <b>Managing the family business</b><br>
            How should privately held corporations handle ownership 
            considerations versus management and operating considerations? 
            Situations in which ownership and operations or management 
            responsibilities are vested in the same people can be devastating to 
            a family or closely-held corporation if not handled wisely. While 
            difficult, the duties of management and the family/stockholder 
            relationships must be kept separate for the good of both the 
            stockholders and the corporation. <br>
            <br>
            Private relationships that interfere with the effective operation of 
            the company can only be harmful and non-productive. Nepotism can be 
            either positive or negative, depending on the capabilities of the 
            individuals involved. It is rarely neutral. Business considerations 
            should take precedence over family considerations when it comes to 
            the welfare of the company. Private, family matters should never 
            interfere in or be a part of the business. Failure to adhere to this 
            advice can often lead to unfortunate or even calamitous situations 
            that can tear a closely held company apart. <br>
            <br>
            These may be difficult situations, and may require an impartial 
            source to mediate. In any case, clear, objective, unambiguous 
            guidelines and goals should be set in writing so that the junior 
            members of the family firm may have specific expectations of where 
            they might go, how they are expected to progress, and the standards 
            by which they will be judged. Family considerations must be kept 
            outside of the on-the-job evaluations, or the entire process can 
            become quite unsettling and less than objective for the individuals 
            involved. <br>
            <br>
            Where possible, outsiders (to the family) might be involved in the 
            mentoring and development processes to help the development of the 
            younger generations. This can bring a degree of objectivity that 
            parents and children can often find difficult to maintain in 
            situations where the younger generation works for and will 
            eventually succeed the senior people. Settling this<img border="0" src="cover3.jpg" align="left" width="200" height="139" hspace="5" vspace="3"> issue is 
            potentially the key to the effectiveness and even survival of some 
            privately held firms. In other words, owners of closely held 
            businesses require guidance from their business advisors to ensure 
            continuity of management and ownership in succeeding generations 
            during the lifecycle of the business. <br>
            <br>
            Entrusting the management of the business to the proper person(s) 
            may determine the ultimate success or failure of the enterprise. 
            Yet, determining which family members are to share in the financial 
            success (or failure) of the business is a separate question that 
            must be answered. Those family members to whom a business owner may 
            wish to transfer the ownership of the business upon his death or 
            retirement (for example, to his children equally) may not be equally 
            capable of running the business. <br>
            <br>
            For this reason, the business advisor might consider separating 
            management control from ownership of the enterprise in designing a 
            proper succession plan for the family business. There are a variety 
            of different control devices that can be implemented for this 
            purpose. Having a working knowledge of these business succession 
            techniques can be an important tool in strengthening an advisor’s 
            relationship with the business owner and add value to the planning 
            process. The flexibility afforded by the corporate laws and bylaws 
            can be successfully used to:</font></p>
                    <ul>
                      <li dir="ltr">
            <p align="justify" dir="ltr"><font face="Verdana" size="1">
            Divide equity ownership equally among the owner’s children; </font></p>
                      </li>
                      <li dir="ltr">
            <p align="justify" dir="ltr"><font face="Verdana" size="1">
            Separate management control from ownership of the corporation; and </font></p>
                      </li>
                      <li dir="ltr">
            <p align="justify" dir="ltr"><font face="Verdana" size="1">
            Give non-voting and inactive shareholders a voice only on 
            fundamental corporate matters. </font></p>
                      </li>
            </ul>
            <p align="justify" dir="ltr"><b><font face="Marlett" size="1">
            h</font><font face="Verdana" size="1">Dispute resolution</font></b><font face="Verdana" size="1"><br>
            Regardless of the control technique implemented, the business 
            advisor must always consider the possibility of future conflict due 
            to the nature of the family business and the varying personalities 
            of its directors, officers, and shareholders. Courts are an 
            expensive forum in which to resolve these disputes. Therefore, 
            including dispute resolution methods in succession planning can be 
            critical to the success of the business in future generations. An 
            outside board of directors or advisors can be created to offer 
            non-binding advice and recommendations to the corporation. <br>
            <br>
            Alternatively, an outside board could be given the power to make 
            binding decisions on any significant disputes that may arise. In 
            addition, buy-sell agreements need not be limited to controlling the 
            ownership of stock upon death, disability, etc… Such agreements can 
            also appoint a third- party arbitrator to break a voting deadlock 
            and can even obligate a dissenting shareholder to sell his shares to 
            the corporation upon the occurrence of specified events. <br>
            <br>
            Unless appropriate planning is implemented to control the ownership 
            and management of the closely held corporation, succession planning 
            for a business owner cannot be completed with any assurance of 
            predictable results. Creative corporate governance techniques can be 
            used successfully to resolve the possibly contradictory objectives 
            of passing the family business to the next generation while ensuring 
            continuity of management and control. <br>
            <br>
            <b>The role of the CEO</b><br>
            The cost and risk to an organisation of an outside CEO appointment 
            is high. Good succession planning can smooth the transition from one 
            CEO to the next. While it is the board’s prerogative to appoint a 
            CEO, it is the CEO’s responsibility to develop a cadre of potential 
            successors. Whether you are a newly appointed first-time CEO or an 
            experienced CEO reappointed for a further term, the question of 
            succession should be addressed virtually from Day One. The average 
            tenure of a CEO today is only around five years; so, time is of the 
            essence. What are the key steps and when should they be implemented? 
            While there is no definitive answer, the following may be a useful 
            guide:<br>
            <br>
            </font><b><font face="Marlett" size="1">
            h</font></b><font face="Verdana" size="1"><b>Agree strategy with board<br>
            </b>Strategy is, of course, fundamental. Without an agreed longer term 
            (five years, typically) strategy, the competencies required of the 
            senior management team cannot be determined. It is this competency 
            set against which the managers must be appraised. Past history may 
            not be fully indicative of future success, as the fund managers are 
            so fond of saying.<br>
            <br>
            </font><b><font face="Marlett" size="1">
            h</font></b><font face="Verdana" size="1"><b>Appraise senior 
            management<br>
            </b>This will not be easy for a CEO appointed from outside the 
            organisation, but even an insider must now look at his/her former 
            peers and subordinates through CEO eyes. The inevitable personal 
            prejudices must be put aside and an objective view taken. Some 
            outside help, such as a formal management appraisal (including 360 
            degree references) by a reputable consulting firm, may be 
            appropriate to supplement the CEO’s own views. <br>
            <br>
            </font><b><font face="Marlett" size="1">
            h</font></b><font face="Verdana" size="1"><b>Implement succession process<br>
            </b>To be successful, the succession planning process must be endorsed 
            by the board and the senior executive team. Executives identified as 
            ‘high potential’ should be given challenging jobs to ensure they 
            develop as quickly as possible. This may also involve a review of 
            the organisation structure to ensure sufficient development roles 
            exist. Some specialised leadership training, additional 
            opportunities for appropriate education and the appointment of a 
            coach or mentor should be seriously considered. It is important to 
            instil a culture that celebrates achievement and support of high 
            achievers without being ‘elitist’. The tendency has been to ‘cut 
            down the tall poppies’ and to look at the lowest common denominator 
            rather than strive to reach the highest. Once the critical few are 
            ‘under way’, attention must be turned to the next level of potential 
            successors. This requires a talent management program. <br>
            <br>
            </font><b><font face="Marlett" size="1">
            h</font></b><font face="Verdana" size="1"><b>Develop talent management<br>
            </b>The enterprise requires generalists at the top. This means giving 
            executives broad exposure to the organisation. Moving managers 
            between functions or functional managers into line roles (or 
            vice-versa) are not easy decisions to make in this world of 
            increasing specialisation. Developing talent from the more junior 
            managerial ranks is, however, important as developing the senior 
            high-fliers. A talent management program that addresses the three 
            areas of recruitment, development and retention is key. The third 
            key element, retention, needs special attention. Opportunity and 
            culture typically play a bigger role than remuneration. This applies 
            both to internal perceptions and to attracting top-class individuals 
            from outside.<br>
            <br>
            </font><b><font face="Marlett" size="1">
            h</font></b><font face="Verdana" size="1"><b>Delegate more, encourage entrepre neurs</b><br>
            While the CEO is ultimately responsible, s/he does not have to be a 
            ‘one-man-army’. True delegation offers the best opportunity for any 
            CEO to meet realistic board expectations in both the short and long 
            term. For the potential CEO candidates to prove themselves, they 
            must be encouraged to try things without the fear of punishment for 
            failure. Controlled entrepreneurship can lead to new ideas and/or 
            fresh ways of looking at things without going outside. <br>
            <br>
            </font><b><font face="Marlett" size="1">
            h</font></b><font face="Verdana" size="1"><b>Review succession with board</b><br>
            The board should, of course, be kept apprised of developments on an 
            ongoing basis and given exposure to the possible CEO candidates. As 
            the time for retirement (or other trigger) approaches, however, an 
            in-depth review should be held with the board to evaluate the pros 
            and cons of each potential successor. Once the preferred candidate 
            has been selected, the transition process can begin. To minimise the 
            disruption of succession, it is important for the incumbent CEO to 
            ensure, as much as possible, that all the difficult aspects, such as 
            the integration of a major acquisition, a significant write-off or 
            downsizing, etc. are well under way before s/he steps down.<br>
            <br>
            Selecting, mentoring and measuring the next generation of enterprise 
            leadership is the CEO’s responsibility. It is a controllable 
            activity and a critical one. CEOs can probably apply the old 80:20 
            rule to the balance between internal and external appointments, but 
            their development and retention are equally critical. Without a 
            formalised succession planning process, it is clear an organisation 
            can suffer badly. </font></p>
