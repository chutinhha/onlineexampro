<p align="justify"><font face="Verdana" size="4" color="#FF0000">Privatisation 
pangs</font><font face="Verdana" size="1"><br>
<br>
<b>Michael Earley delves into the contentious issue of employment continuity as 
a result of privatisation of public utilities</b><br>
<br>
</font><font face="Verdana" size="3"><b>
<img border="0" src="pre.jpg" align="left" width="112" height="132">S</b></font><font face="Verdana" size="1">ince 
its accession to the World Trade Organisation (WTO), Oman has embraced a 
programme of privatisation with respect to its various public utilities. Sultani 
decree 77/2004 promulgating the Privatisation Law of Oman (Privatisation Law) 
demonstrates the government’s commitment to its programme to diversify sources 
of national income, broaden the Oman production base, mobilise market forces and 
promote competition, relieve financial and administrative burdens on the 
national budget to allow the government to undertake strategic investments, and 
increase the domestic employment opportunities for Omani nationals.<br>
<br>
Contrary to the goals specifically provided in the language of the Privatisation 
Law, many employees of public utilities scheduled for privatisation are 
concerned that privatisation will result in the termination of their employment. 
This reflects a concern which is felt generally around the globe. But is this 
concern justified?<br>
<br>
<b>Transfer to private company</b><br>
When a public utility has been scheduled for privatisation (the project), a 
tender for the provision of that utility by a private company is floated in 
accordance with the Tender Law. Normally, those parties interested in tendering 
for the project will establish a project company. In order to prevent 
monopolisation and to promote competition, the Privatisation Law permits more 
than one project company to be established in order to provide the service or 
utility that is scheduled for privatisation.<br>
<br>
These project companies must then submit a tender bid for the provision of the 
utility or service to be privatised. As part of their tender bid, these project 
companies are required to identify those existing employees of the public 
utility that they intend to employ subsequent to privatisation. The tender must 
specify the number of employees to be employed, the posts that they will hold, 
and their designations.<br>
<br>
So what is the status of these employees once they are transferred? Fortunately, 
the Privatisation Law directly addresses how the transferred employees are to be 
treated by the project company. The project company will be obligated to enter 
into new employment contracts with each transferred employee identified in the 
tender bid. The terms of the new employment contract, including the wage to be 
paid and other financial benefits of the transferred employees, may not be less 
than the benefits such employees received prior to their transfer to the project 
company.<br>
<br>
Article 23 of the Privatisation Law is particularly significant as it provides 
that once a transferred employee has been hired by the project company, the 
project company may not terminate such employee’s employment for a period of 
five years from the date of his transfer, provided that the transferred employee 
abides by the rules and regulations of employment in the project company.<br>
<br>
Furthermore, those employees who are transferred to the project company continue 
to be required to pay contributions under the applicable retirement scheme. 
Although these transferred employees will be working for a private company, upon 
termination for any reason, they will be regarded as governmental employees for 
the purposes of determining their end of service benefits.<br>
<br>
<b>Other employees</b><br>
But what happens to those employees who are not identified by the project 
company for transfer in its tender bid? Article 26 of the Privatisation Law 
specifically provides for the various ways of dealing with employees whose 
employment will not be taken over by the project company. The article divides 
non-transferable employees into two categories: those employees who wish to be 
transferred to other governmental entities; and those employees who are neither 
employed by the project company nor transferred to governmental entities.<br>
<br>
Former public utility employees who wish to be transferred to another 
governmental entity will be required to undergo retraining and instruction 
according to the requirements of their new employment. Former public utility 
employees who have completed 10 or more years of service with the public utility 
retire, and their rights will be determined on the basis that they have reached 
the age of retirement in accordance with the retirement pension scheme that was 
applicable to the utility prior to privatisation.<br>
<br>
Furthermore, each former employee shall be given a gratuity equivalent to one 
months’ Basic wage for each year of service based on the last wage received with 
a maximum of 12 times such Basic wage and a minimum of RO 5,000. The 
Privatisation Law permits those employees whose period of service falls short of 
the 10 year minimum by 6 months or less to purchase the shortfall period for the 
purposes of pension entitlements by paying to the pension fund whatever 
contributions may be due in respect of the remaining period.<br>
<br>
Those employees who have not completed at least 10 years of service and are not 
entitled to a retirement pension shall each be given an end of service gratuity 
calculated at the rate of 4 times their last received Basic monthly wage, for 
each year of service. Additionally, any fixed allowances shall be added to this 
amount, but this gratuity will be reduced by the value of the end-of-service 
benefit due to such employee according to the pension scheme that was applicable 
prior to the utility’s privatisation. If an employee’s period of service is less 
than one year, such period shall be deemed to be one full complete year for the 
purposes of determining his gratuity.<br>
<br>
<b>Win/Win</b><br>
Whilst it is possible that not all of the former employees of a public utility 
will be transferred to the project company, the compensation scheme contained in 
the Privatisation Law appears to sufficiently provide for such employees. 
Privatisation and the establishment of more project companies will not only 
preserve many of the existing jobs of Omani employees working for public 
utilities, but will also create new employment opportunities in such companies 
for other Omanis. <br>
<br>
