<img border="0" src="omanexpo-1.jpg" align="left" hspace="5" width="250" height="187">Delegates 

visiting Oil and Gas West Asia (OGWA) 2004, the region's established oil and gas 

exhibition and conference, are likely to strike it lucky as they network with 

exhibitors and other industry officials from West Asia's petrochemicals industry 

showcasing the latest products or services of their respective companies. The 

event, which will take place between April 19 - 21 at the Oman International 

Exhibition Centre, will also be a great opportunity to have a crafty look at 

what your competitors are marketing, say organisers of OGWA. <br>

<br>

&quot;The show provides the ideal meeting place for the region's oil and gas industry 

to convene and discuss business,&quot; says C.J. Paul, general manager of Omanexpo. 

&quot;This is the most exciting time there has ever been for the energy business so 

we are excited about the industry response to OGWA 2004.&quot; <br>

<br>

&quot;What makes OGWA important to the oil and gas industry is the unique perspective 

it has on developing business. OGWA provides sellers with a unique opportunity 

to meet with customers in a friendly, efficient and neutral environment. Chances 

are you will meet with senior management or specifiers and previous exhibitions 

have proven that OGWA is one of the best investment any exhibitor can make,&quot; he 

