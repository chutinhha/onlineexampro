<br>

<b>Timing is everything </b><br>

Sure enough, there has never been a better time to host an energy show in the 

region. The Organisation of Arab Petroleum Exporting Countries (OAPEC) reported 

in mid-2002 that $113 billion would be required from the period 2003 to 2008 to 

enable Arab energy producers alone to expand their oil, gas and petrochemical 

industries. Oman's Ministry of Oil and Gas has already announced at least $6.5 

billion of new internal energy investment from 2002 to 2005. The figures stack 

up hugely when you consider the level of spending required across the region. 

Iran reportedly needs to invest some $30 billion during the same period, 

Pakistan $4-6 billion and India $9.5 billion, according to latest studies.<br>

<br>

Although Russia's oil reserves will play an important role in the future, 

experts have repeatedly warned that the US will become heavily reliant on Middle 

East oil in the medium term as its own crude wealth will eventually be depleted, 

its domestic consumption is growing fast, and its non-Middle East supply sources 

are dwindling. BP plc puts the US's proven oil reserves at around 30.4 billion 

which could be enough for around 10 years at current production rates of nearly 

7.6 million barrels per day (bpd).<br>

<br>

According to a report at the end of last year, at current production rates of 

around 2.3 million barrels per day, the UAE's oil reserves could last more than 

100 years to make it among three countries whose proven crude wealth could 

extend over a century. The other two are Iraq and Kuwait. <br>

<br>

<b>Stepping on the gas</b> <br>

We already know that gas looks certain to replace oil as the main source of 

power in the future. Worldwide consumption of gas is well over one billion 

tonnes each year. Gas supply looks set to expand until 2090, when output will be 

5.5 times current levels.<br>

<br>

Reports also suggest that the world needs $16 trillion worth of investment in 

energy over the next 30 years if it is to meet growing demand for supplies. The 

bulk of the $16 trillion would be needed in power generation, transmission and 

distribution with a further $4 trillion on upstream investment in oil and gas to 

keep output at existing levels. But some investors are already being scared away 

from projects because liberalisation has made the levels of return much less 

certain, the International Energy Agency warns. <br>

<br>

Recent power outages in the US have highlighted the problems of keeping 

electricity transmission networks up to scratch but investment in gas-fired 

plants has faltered. &quot;Without new policy actions, world energy demand will rise 

by two-thirds between now and 2030 and the world economy will falter if these 

energy supplies are not made available,&quot; says a senior IEA spokesperson. <br>

<br>

Developing nations face the biggest energy challenge to attract some of the $5 

trillion of new investment needed globally in the electricity sector. There will 

be a particular need for the Middle East, Africa and Russia to reform their tax 

conditions and lift impediments to foreign investment because that is where 

global gas reserves are concentrated. There are a host of new technologies being 

developed that offer great opportunities in the future - such as hydrogen and 

fuel cells - but their speed of development depends on changes in fiscal 

incentives offered by governments. <br>

<br>

Closer to home, gas production is witnessing tremendous growth as the Omani 

government continues to look at diversifying its income base from oil revenue. 

Oman LNG is producing from two trains with a total capacity of 6.6 million 

tonnes per year. However, the company is currently developing its third train 

which will increase the output by 50 per cent to 9.9 million tonnes per year. 

The train, which is under construction at a cost of $600 million, is expected to 

begin operating in early 2006. The new train will be owned by Qalhat Gas Company 

which the government has the majority share while Oman LNG and Union Fenosa of 

Spain hold smaller stakes. <br>

<br>

Within Oman, gas availability to Salalah and to Sohar is attracting interest 

from international companies to the industrial estates and downstream cluster 

developments within the country. It will also help a number of existing 

industries economise their operations when they switch to natural gas. <br>

<br>

Meanwhile, a new gas pipeline from Oman linking with the new Dolphin Project 

pipeline that connects gas networks between the UAE and Oman has been connected. 

Oman Oil Company delivering the gas to Fujairah until Dolphin gas from Qatar 

comes on stream in 2006. This link is the most successful cross-border 

development project in the GCC. Moreover, the Dolphin Project is one of the 

largest gas developments in the world with a capital investment of $3.5 billion 

to develop the pipeline and other upstream elements. Dolphin Energy Limited, the 

company managing the project, will be one of the many companies exhibiting at 

OGWA 2004. OER is the official business magazine of OGWA 2004. </font></p>



