                    Cover Story<br>
                    </font><font size="4" color="#FF0000" face="Verdana">The 
            future </font><font face="Verdana" size="4" color="#FF0000">for 
            tourism</font><p>
            <font face="Verdana" size="1"><b>A wave of initiatives to promote 
            tourism in Oman is underway spelling good news for the holiday 
            trade. But can the country capitalise on the momentum that is being 
            generated? Nayl D'Souza reports </b></font></p>
            <p align="center">
            <img border="0" src="cs_tours.jpg" width="450" height="158"></p>
            <p align="justify"><font face="Verdana" size="1">You're on a cable 
            car soaking up breathtaking mountainside views of the shoreline. 
            Yesterday you admired the beauty of coral and exotic fish from a 
            semi-submersible vessel and later in the day you watched a world 
            famous tall ship race. Can you guess where you are? Well, this is 
            Muscat-albeit within a decade if parts of the new tourist landscape 
            envisaged by the government fructify. Cynics who believe that Oman 
            will be too late to the party should at least consider the thrust 
            that has already been developed in the past few years. Before the 
            end of next year, Oman's first PGA 18-hole golf course and the Barr 
            Al Jissah resort will open for business. Small steps perhaps, but 
            this is just within the capital area. Elsewhere in the country, the 
            government has plans to advance a range of eco-tourism, adventure 
            tourism and heritage projects. For example, a RO2.5 million scheme 
            is already underway enabling visitors to journey by train to the 
            centre of Al Hoti Cave from March 2005. <br>
            <br>
            As exciting as this may sound, it isn't enough to send the pulses 
            racing of many tourism professionals in Oman-or, for that matter, 
            cash tills ringing. <br>
            <br>
            The industry has applauded the progress that has been made so far to 
            put Oman on the global tourism map. Indeed, despite low-key 
            promotions over the past few years due to geopolitics, the country 
            has attracted significant tourism interest. The recently announced 
            marketing budget for the promotion of the country to the tune of $30 
            million up to the end of 2005 will lift the number of tourist 
            arrivals and pump their expenditure into the local economy. But 
            plainly some fundamental issues need to be addressed if Oman is 
            going to reap further dividends. <br>
            <br>
            <b>Slow but steady growth </b><br>
            Unlike some of its counterparts, Oman is a stunning destination. 
            Visitors receive great hospitality and can savour authentic history 
            and traditions. Oman's cultural heritage is evident in its 500 or 
            more forts, castles and towers depicting its vibrant past. These 
            have also proved to be a major attraction and efforts are being made 
            to renovate historical monuments to their former glory. Oman's Forts 
            and Castles Restoration Project is focussing on three locations at 
            present: Khasab Fort, Al Hazm Fort and Nizwa Fort. <br>
            <br>
            While tourism development remains cautious and security concerns 
            remain paramount, a large number of industry officials believe that 
            the country has nothing to fear by opening its doors a little wider 
            to tourism. <br>
            <br>
            &quot;Tourists usually spend a ridiculous amount of money on souvenirs 
            and antiquities. Omanis could share their cultural heritage with 
            international visitors and sell mementos. This kind of culture 
            tourism will actually promote and preserve the country's heritage,&quot; 
            maintains Sunita Gomes, manager - Oman, British Airways. <br>
            <br>
            Local entrepreneurs and artisans could definitely exploit such 
            business opportunities.<br>
            <br>
            Gautam Broota, general manager of Zubair Tours, agrees that Oman 
            could be a little less selective with its tourism policy. &quot;Tourists 
            visiting Oman are seemingly worldly-wise. They are usually over 50 
            years of age and have previously toured many other parts of the 
            world before deciding to visit.&quot; He urges that the country target a 
            wider variety of tourist categories rather than focus on specialist 
            segments such as eco-tourism. &quot;Fortunately, the country is 
            attracting tourists who are moneyed and are willing to spend when 
            they arrive. However, they would afford to pay for greater comforts 
            if offered to them.&quot; <br>
            <br>
            <b>The defining moment <br>
            </b>Needless to say, Oman is at a crossroads. Does it continue with 
            its modernising agenda and take a more risky approach to tourism 
            development that throws more caution to the wind but sends a 
            brighter green light to foreign investors? Or, does the country 
            instead continue to follow a more traditional cautious approach 
            which may restrict tourism growth and limit potential job creation 
            but protect Oman's cultural and social identity in the process? <br>
            <br>
            Worryingly, the nation hasn't much time to decide which path to 
            take. <br>
            <br>
            Oman has just over 17 years of oil production left before its oil 
            reserves are depleted by virtue of a very 'crude' OER estimate based 
            on the country's oil reserves and its production average in 2002. Of 
            course, new oil discoveries can arise at any time and dramatic 
            improvements in extracting the 30 per cent of Oman's stored oil in 
            place that is not inconceivable to produce could delay this 
            depletion date. The calculation also negates the confidence 
            expressed by His Excellency Dr Mohammed Al Rumhy, minister of oil 
            and gas, to MEED magazine in May 2001 that the Omani oil and gas 
            industry &quot;will be in business for another 50 years.&quot; <br>
            <br>
            <b>How badly are tourists wanted? </b><br>
            Whatever the actual figure for oil reserves, long term the 
            government hopes that the income from oil and gas can be used to 
            deftly accelerate its diversification process. Oman has proceeded 
            swiftly on a number of downstream diversification projects 
            constantly in the news. <br>
            <br>
            Similarly, tourism is being prioritised for an enhanced role in this 
            process. &quot;Within the sixth Five-Year Plan, Oman's government aims to 
            further mobilise the private sector to develop tourism, promote the 
            tourism sector in line with sustainable principles, achieve regional 
            development, reach an Omanisation rate of 50 per cent within the 
            industry and find alternative sources for financing the sectors' 
            initiatives,&quot; informs His Excellency Mohsin Al Balushi, 
            undersecretary for tourism, Ministry of Commerce and Industry (MCI).<br>
            <br>
            International Development Ireland (IDI) Ltd was commissioned by the 
            MCI in July 2002 to foster Oman's tourism economy in a fast-track 
            manner. As the company's name suggests, IDI has been a major player 
            responsible for lifting the travel earnings of Ireland's tourism 
            sector to nearly RO2.6 billion in 2002 from around RO1.1 billion in 
            1990. This was a remarkable performance bearing in mind that for 
            some of this period the political fallout from the conflict in 
            Northern Ireland gripped the country. <br>
            <br>
            <b>Becoming a contender </b><br>
            <b>
            <img border="0" src="cove2.jpg" align="right" hspace="5" width="200" height="133"></b>Currently, 
            Oman is looking to expand tourism distribution channels. &quot;We must 
            get more coverage in the brochures of travel operators,&quot; says Jim 
            Flannery, team leader of IDI's support team at the MCI. &quot;Secondly, 
            more awareness must be raised among sales representatives as well as 
            to potential tourists overseas, to generate more interest in inbound 
            tourism.&quot; Oman is also focussed on re-branding its image and 
            perception among would-be international visitors although extra 
            research in foreign markets will be conducted before a new logo and 
            strap line are chosen. <br>
            <br>
            &quot;Tourism will become very important for Oman. It is the most diverse 
            quality tourism products on offer in the Middle East. Once the 
            marketing begins to have a multiplier effect, you will notice that 
            its contribution to the national income will kick in,&quot; agrees 
            Mohammed al Sinani, director of planning and tourism development for 
            the MCI.<br>
            <br>
            When The Wave resort project in February 2004 was announced, the 
            spotlight fell on the liberalisation of Oman's real estate market. 
            It was pronounced that citizens from the GCC could own or rent land 
            in Oman. The local media also proposed that a decision to allow 
            international freehold ownership was in the offing. At the time of 
            going to press, no such law has been passed but many anticipate that 
            it is fast approaching. <br>
            <br>
            &quot;The imminent change in law allowing international freehold property 
            sales in Oman is probably one of the most important investment 
            decisions that the country has ever made,&quot; says Charles Rowe, 
            project director, Muscat Golf &amp; Country Club. &quot;The move will have a 
            huge impact bringing in investors that had never contemplated 
            spending here before, as well as high net worth second-home buyers 
            and retired people.&quot; <br>
            <br>
            <b>Facilitating greater access </b><br>
            The airlines play their role to support Oman's tourism industry. The 
            fact that Gulf Air will be flying non-stop to Muscat from London 
            from June 21 is another helping hand. Gulf Air already operates the 
            highest number of flights out of Muscat to GCC states than any other 
            carrier. <br>
            <br>
            &quot;Of course, this will minimise the number of flying hours that a 
            passenger has to endure on a long haul flight,&quot; says Jaffar Juma, 
            sales manager, Gulf Air. &quot;Having recognised the growing potential of 
            the German tourist market, our airline has also announced a daily 
            direct flight to Muscat from Frankfurt.&quot; <br>
            <br>
            &quot;London is probably the only European destination that could sustain 
            business for a non-stop flight from Muscat and it is today a 
            destination already crowded by competing airlines,&quot; says Peter 
            Schilter, manager - Oman, SWISS. <br>
            <br>
            Such a fight for custom does not affect SWISS as much because the 
            airline's focus is on central and southern Europe. &quot;Sure we would 
            like to benefit from more traffic to London, but we will not enter 
            into a price war for the UK segment,&quot; he adds.<br>
            <br>
            <b>Have visa, will travel </b><br>
            In a bid to ease travel restrictions and boost inbound tourism, Oman 
            recently introduced simplified visa procedures where visitors from 
            75 countries are eligible for visas on arrival at the country's 
            entry points. Eligible visitors include those from almost all 
            European nations, the Americas, Australia and the Far East. Plus, 
            Oman has joint visit facilities with Qatar and Dubai. <br>
            <br>
            Kevork Deldelian, general manager, Crowne Plaza Muscat, points out 
            that the changes in the visa regulations pose certain difficulties. 
            &quot;A number of our Lebanese customers experienced travel difficulties 
            trying to enter Oman while other clients of ours have been stopped 
            at border points for many hours before they can proceed.&quot; <br>
            <br>
            The speed to which visas are processed at the airport is so poor 
            that some hotels like the Al Bustan tend to organise them for their 
            guests before they travel. &quot;It can still takes up to an hour to get 
            through immigration control even if the visa has been issued because 
            of the pattern of flights, the volume of people, the number of desks 
            open and the processing of passports,&quot; explains Malcolm Rann, 
            general manager, Al Bustan Palace hotel. <br>
            <br>
            For some in the tourism industry, there isn't nearly enough 
            attention paid to less obvious hurdles facing the industry. &quot;These 
            are basic tourist problems and there are others that are also being 
            overlooked,&quot; says M. K. Mohamed, general manager of Mark Tours. &quot;For 
            example, the availability of maps is poor. Worse still, when a 
            tourist travels to Wahiba Sands or to Jebel Shams, there are very 
            few facilities along the way, particularly restrooms.&quot; <br>
            <br>
            <b>More tourism, more training </b><br>
            The availability of local staff trained to European 'fine' 
            hospitality standards from special dedicated tourism academies is 
            something that Oman is happy to publicise. However, if the industry 
            is to steer towards a higher growth trajectory, it will need to 
            produce many more skilled workers. &quot;Oman's new tourism projects that 
            are being proposed and developed are great assets to Oman but we 
            need to look at the development of Omanis to serve the hospitality 
            industry as well,&quot; says Torbjorn Bodin, general manager, Radisson 
            SAS Muscat. &quot;It is questionable whether these resort projects can 
            find, employ and retain adequately trained staff.&quot; <br>
            <br>
            The National Hospitality Institute and the National Academy for 
            Hospitality and Tourism as well as a number of leading hotels in the 
            country will continue to do their utmost to nurture talent but may 
            not be enough bearing in mind the Omanisation levels envisaged for 
            the industry in years to come as well. <br>
            <br>
            <b>Investors take a break </b><br>
            Oman's government has had to jump start the tourism sector while 
            investors seemingly take a holiday. Outside of Muscat, RO3.3 million 
            of state investment has been made on two hotels; a four-star 60-room 
            hotel in Khasab and a three-star 32-room hotel in Dibba. Of course, 
            the government also recently stepped in to support the Zubair Group 
            in its bid to create the Barr Al Jissah resort opening in July next 
            year. <br>
            <br>
            There are many reasons why foreign investors should decide to choose 
            Oman as an investment location. The country offers investors a 
            50-year renewable lease, reduced rental rates, and soft loans with 
            low interest rates and repayment grace periods. If that wasn't 
            enough there's income tax relief for up to 10 years, no restrictions 
            on profits transfer or capital import, up to 70 per cent foreign 
            ownership (100 per cent in certain cases) and plenty of support to 
            process approvals and licenses. <br>
            <br>
            <b>Showcasing Oman </b><br>
            There are other ways to lure investors and tourists. An ambitious 
            mission by the Omani Centre for Investment Promotion &amp; Export 
            Development (OCIPED) is underway to market Oman as an exciting 
            location for movie making. Initially, the project will aim to 
            attract potential film makers via a website but other promotional 
            activities are in the pipeline. Previously in Oman, a number of 
            Bollywood directors have chosen luxury hotels as the backdrop to 
            some of their latest releases. Blockbusters these may not have been, 
            but every snatch of celluloid the country can grab helps. <br>
            <br>
            Aside from boosting tourist recognition, film making can become a 
            very lucrative money-spinner. For example, it is estimated that in 
            the UK, ú410 million (RO250 million) was poured into British films 
            by foreign investors in 2003. Some may be sceptical that Oman could 
            rival the UK in the same way and become the setting for some future 
            Oscar winning movies. True, the Sultanate may not have technical 
            pre- and post-production crews in situ as was the case in New 
            Zealand for recent Hollywood epics like the Lord of the Rings 
            trilogy and Whale Rider, but it does have an abundance of beautiful 
            locations (most never captured on the 'silver screen') as well as 
            plenty of willing candidates to serve as extras. This last point was 
            highlighted last month at the Al Bustan Palace hotel as it became 
            the lavish set for Traumschiff (translated as 'Dreamboat'), a 
            popular German television series centred around the luxury cruise 
            ship. The producers of the show televised on German state television 
            channel Zweites Deutsches Fernsehen (ZDF) were enticed by Oman's 
            government for an undisclosed fee to film a special episode at the 
            hotel which is expected to be aired at prime time during Christmas 
            in Germany. Over 20 million television viewers are expected to tune 
            in. <br>
            <br>
            Meanwhile, sometime this month Travel Channel will broadcast a 
            30-minute guide to Oman. As part of a deal struck with the MCI - 
            again for an undisclosed sum - the show will be the first of 30 to 
            be broadcast during the next 3 years. Travel Channel primarily 
            broadcasts via satellite to Europe and parts of the Middle East. The 
            programmes will be updated and the MCI will co-own all the footage 
            including shots that are not broadcast.<br>
            <br>
            Because Oman can't spend its coffers on a huge advertising campaign, 
            these moves to get Oman seen on television shows in key markets 
            aboard seems to be the next best option. &quot;We don't have a budget 
            where we can run a massive consumer campaign. The budget is 
            satisfactory to target operators abroad,&quot; says Flannery of IDI. 
            Nonetheless, there is a lot of emphasis on getting editorial 
            publicity by inviting travel writers to review what there is on 
            offer here. <br>
            <br>
            To be fair, Oman is getting its fair share of critical acclaim from 
            the write-ups. Surprisingly, many give Oman first-choice status over 
            Dubai. A writer earlier this year in the UK-broadsheet The Sunday 
            Times observes: &quot;The appeal of Dubai remains a mystery to many and 
            never more so than now when the construction of the two Palm Islands 
            and The World means the swankiest beaches overlook cranes and 
            scaffolding. Which is why the smart money is on a defection to 
            neighbouring Oman. Not only are the beaches better, they have a 
            spectacular mountain backdrop and offshore, some of the world's best 
            dive sites.&quot; <br>
            <br>
            Local industry officials at ITB Berlin, the leading trade fair for 
            the global tourism industry held last month, have received similar 
            positive feedback. &quot;The response to ITB Berlin has been phenomenal 
            this year compared with 2003. The Sultanate is certainly being 
            viewed as somewhere fresh and inviting,&quot; says Mohamed of Mark Tours.
            <br>
            <br>
            Despite this, Dubai is streets ahead of the regional competition and 
            it hopes to attract 15 million visitors a year by 2010. &quot;The reason 
            Dubai is successful is because it has a plethora of things for 
            tourists to do. Oman doesn't and this fundamental problem needs to 
            be addressed,&quot; argues Rowe of Muscat Golf &amp; Country Club. <br>
            <br>
            In spite of its apparent dullness in comparison to Dubai, Sabreen 
            Sabry, manager - UAE, Oman Air, notes: &quot;Due to more than 10,000 
            years of history, its location and easy access, Oman is becoming a 
            regular weekend destination for UAE expats.&quot; <br>
            <br>
            Oman Air offers holiday packages to Muscat and other destinations in 
            Oman, and operates up to seven flights a day from Dubai and Abu 
            Dhabi to Muscat as well as direct flights from Dubai to Khasab and 
            Salalah. <br>
            <br>
            In contrast, there are no guarantees that SWISS, a company that is 
            very much still in turnaround mode, will continue to operate in 
            Oman. <br>
            <br>
            The airline's services to Europe from Muscat are reliant on about 
            two-thirds of its passenger load from Dubai. Therefore, it will 
            ultimately be the transport of tourists to and from Dubai that will 
            determine the fate of the European service which the company 
            currently operates via Muscat. <br>
            <br>
            <b>Exchanging ideas </b><br>
            Industry forums in Oman to rectify problems, identify pitfalls and 
            exchange ideas have been happening in the past and it is encouraging 
            to note that these discussions are happening with greater frequency 
            of late. The danger, however, is that these meetings on improving 
            Oman's tourism sector simply become 'talking shops'.<br>
            <br>
            &quot;I feel it is now time for dialogue to deliver more progress,&quot; 
            remarks Sanjeev Tandon, director of sales and marketing, Sheraton 
            Oman hotel.<br>
            <br>
            &quot;I agree that there needs to be more alignment between the public 
            and private sector. The government doesn't sell one airline ticket 
            to Oman-the industry does. We support the private sector 
            wholeheartedly,&quot; says Flannery. <br>
            <br>
            <b>MRA gets a mixed reception </b><br>
            There are other isssues facing the industry too. From May 1, all 
            hotels in Oman will comply with fixed tariffs as per the Minimum 
            Room Agreement (MRA) scheme being implemented and policed by the 
            government. The proposal is yet to be fully ironed out but the hotel 
            sector is preparing to play ball. It may avoid a price war in the 
            summer months between the hotels but there is still mixed reaction 
            to the scheme, particularly in regard to its effectiveness, its 
            flexibility and its potential success. Under Oman's MRA, hotels will 
            have to charge a minimum amount for a room based on the property's 
            star-grade, the type of tourist and time of year.<br>
            <br>
            Most of the hoteliers were unable to comment as they represent 
            international chains that endorse free market systems rather than 
            price fixing but those that did mainly agreed that the scheme may be 
            workable and, more than this, could actually help. <br>
            <br>
            &quot;Those hotels which rely on leisure tourists may not win as much as 
            those that focus on corporate customers,&quot; forecasts Munir Ahmed, 
            general manger, Grand Hyatt Muscat.<br>
            <br>
            Another GM of a Muscat-based hotel confided in OER that the owner of 
            the hotel would rather risk facing large fines than abide to the 
            pricing regime. <br>
            <br>
            <b>The future is... </b><br>
            Anyone writing Oman's tourist position off will do so at his 
            <b>
            <img border="0" src="im2.jpg" align="right" hspace="5" width="150" height="227"></b>or her 
            peril. Despite its slow momentum, the country's long game strategy 
            could be a case of the tortoise winning the race. But the challenges 
            facing Oman are diverse. Shanker Bose, general manager of Bahwan 
            Travel Agencies, reminds us that: &quot;When it comes to tourism 
            development, one size does not fit all countries.&quot; <br>
            <br>
            However, there's an excitement among tourists that do arrive here 
            that is unmatched by any of its regional counterparts. More often 
            than not, they return to Oman which is a promising sign of guest 
            satisfaction. <br>
            <br>
            Many within the business community echo the view that Oman offers a 
            more authentic Arabian experience than most regional destinations. 
            They are encouraged to see that more golf courses are planned as 
            this will satisfy upscale tourism. <br>
            <br>
            For a country that opened up to tourism a few decades ago, Oman has 
            made great strides in catching up with the modern world. If the 
            country is able to step up aggressively to promote new touristic 
            enterprises, if the political climate in the region stabilises and 
            if the domestic investment climate is enhanced further, Oman could 
            still emerge as a leading tourist destination.</font></p>
