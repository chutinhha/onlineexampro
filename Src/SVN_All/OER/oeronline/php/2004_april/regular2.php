<p><font face="Verdana" size="4" color="#FF0000">Directors, beware!</font></p>

<p align="justify"><font face="Verdana" size="1"><b>Ali Hassan Moosa warns some 
companies in Oman that there are still more lessons of corporate governance that 
could be learnt, followed and adopted</b></font></p>

<p align="justify"><font face="Verdana" size="1">
<img border="0" src="bank1.jpg" align="left" hspace="5" width="150" height="177">Good 
corporate governance and effective regulation contribute towards the 
attractiveness of the country in terms of inward investment and demonstrates the 
efficiency of the capital market by enhancing its role in the development of the 
national economy. Corporate governance is not a static code of conduct but 
continuously evolving. <br>
<br>
It seems that most directors are successful people who have excelled, but they 
need to fully appreciate the momentous task they are undertaking. Directors, in 
theory, must balance the expectations of shareholders with those of the 
regulatory authorities. <br>
<br>
The need for directors to fully understand their legal duties and 
responsibilities is of paramount importance. It has been evident, both locally 
and elsewhere, that some public company directors are not aware of their onerous 
duties imposed by the law. <br>
<br>
Generally speaking, a director's mandate will normally include (a) to act in 
good faith in the best interest of the company (b) to avoid conflict of interest 
(c) to exercise power for the right purpose (d) to exercise due care in carrying 
out responsibilities. <br>
<br>
Unfortunately, I believe we continue to witness shortfalls in director 
performance in Oman due to a number of reasons, but mainly because: <br>
</font><font size="1" face="Marlett">h</font><font face="Verdana" size="1"> 
Directorship sometimes is honorary rather than being based on 'pure' shareholder 
interest. <br>
</font><font size="1" face="Marlett">h</font><font face="Verdana" size="1"> A 
number of directors are self-employed businessmen. Oman being a relatively small 
market raises a doubt on whether conflicts of interest are evident.<br>
</font><font size="1" face="Marlett">h</font><font face="Verdana" size="1"> Some 
directors do not have the proper qualification and experience for the job. <br>
</font><font size="1" face="Marlett">h</font><font face="Verdana" size="1"> 
Hardly any veteran bankers are on the panel of directors in local banks. <br>
</font><font size="1" face="Marlett">h</font><font face="Verdana" size="1"> 
Serious disagreements on major issues between directors and management result in 
the directors and management moving into two different directions. </font>
<font size="1" face="Marlett"><br>
h</font><font face="Verdana" size="1"> Some directors are seemingly focused 
primarily on dividends rather than core issues such as company growth and 
earnings sustainability. <br>
</font><font size="1" face="Marlett">h</font><font face="Verdana" size="1"> Some 
directors lack the fundamentals of their primary duties and responsibilities.
<br>
<br>
The narrow prospective needs to be changed. It is time that the Board of 
Directors of local banks and finance companies form a more broader mixed bag of 
experts comprising of more bankers and financiers. Financial intermediaries are 
exposed when a knowledge gap exists between senior management and directors. <br>
<br>
Though the board is to be viewed as a management partner rather than the 
historical role of a policeman, directors ought to keep a close eye on some of 
the warning signs, such as: <br>
</font><font size="1" face="Marlett">h</font><font face="Verdana" size="1"> 
Unrealistic earnings expectations by shareholders and the market; </font>
<font size="1" face="Marlett"><br>
h</font><font face="Verdana" size="1"> Unrealistic growth and earnings; </font>
<font size="1" face="Marlett"><br>
h</font><font face="Verdana" size="1"> Regulatory violations and concerns;
</font><font size="1" face="Marlett"><br>
h</font><font face="Verdana" size="1"> Frequent organisational changes; </font>
<font size="1" face="Marlett"><br>
h</font><font face="Verdana" size="1"> High turnover of staff, specifically of 
senior management; </font><font size="1" face="Marlett"><br>
h</font><font face="Verdana" size="1"> Unqualified and under-performing 
management; </font><font size="1" face="Marlett"><br>
h</font><font face="Verdana" size="1"> Management ethics and conduct; </font>
<font size="1" face="Marlett"><br>
h</font><font face="Verdana" size="1"> Management override of board issues;
</font><font size="1" face="Marlett"><br>
h</font><font face="Verdana" size="1"> Untimely reporting to the board; </font>
<font size="1" face="Marlett"><br>
h</font><font face="Verdana" size="1"> Excessive risk exposures (For example, 
credit, liquidity, interest rate, and foreign exchange). <br>
<br>
The audit committee is an essential part of the board and the corporate 
governance regime. Its primary responsibility is to oversee on behalf of the 
board the integrity of the financial reporting and internal controls. The audit 
committee may concentrate on the following: <br>
a. Financial reporting including disclosures. <br>
b. Risk management and internal controls. <br>
c. Internal audit process. <br>
d. External audit process. <br>
e. Performance of the firm and other issues. <br>
f. Communication to the board and market. <br>
<br>
Regulatory compliance. <br>
Following the world's recent financial scandals that have hit the headlines , 
audit committees across the globe are under immense pressure to provide 
effective oversight of the financial reporting of the company. However, there is 
a danger that audit committee members may be viewed as the only experts 
responsible for keeping the company honest. <br>
<br>
Audit committees are facing a time of change due to higher expectations from 
shareholders and regulators alike. The popular belief is that the committee 
should enforce the highest standards of sincerity and accountability. Below are 
some of the internationally accepted ground rules for audit committees: <br>
</font><font size="1" face="Marlett">h</font><font face="Verdana" size="1"> 
Focus on major issues affecting the company. </font>
<font size="1" face="Marlett"><br>
h</font><font face="Verdana" size="1"> Not to micro manage the company. </font>
<font size="1" face="Marlett"><br>
h</font><font face="Verdana" size="1"> Should not behave like auditors or 
lawyers. </font><font size="1" face="Marlett"><br>
h</font><font face="Verdana" size="1"> Avoid overload of responsibilities.
</font><font size="1" face="Marlett"><br>
h</font><font face="Verdana" size="1"> Enhance management effectiveness. </font>
<font size="1" face="Marlett"><br>
h</font><font face="Verdana" size="1"> Strengthen internal controls. </font>
<font size="1" face="Marlett"><br>
h</font><font face="Verdana" size="1"> Look forward not backward. </font>
<font size="1" face="Marlett"><br>
h</font><font face="Verdana" size="1"> Follow realistic meeting schedules and 
agendas. </font><font size="1" face="Marlett"><br>
h</font><font face="Verdana" size="1"> Learn the company and its business.
</font><font size="1" face="Marlett"><br>
h</font><font face="Verdana" size="1"> Be constructive sceptics. <br>
<br>
As per rulings by the Capital Markets Authority (CMA) in Oman, the Board is to 
be comprised largely by non-executive directors (part-time directors who do not 
withdraw a monthly fixed salary). At least one third of the board (subject to a 
minimum of two directors) must comprise of independent directors (directors who 
do not have a direct family member as a key employee and do not have a material 
transaction relationship with the firm or any of its associated companies). <br>
<br>
According to the CMA, any audit committee must comprise at least of three 
members, all being non-executive, and the majority of whom to be independent. 
The chairman of the committee must be an independent director. At least one 
director must have accounting and financial expertise. <br>
<br>
Most corporate governance regulations set by the CMA are based on 
internationally accepted best practices, but with a local flavour. It is worth 
noting the comparison between CMA regulations and an international exchange such 
as the New York Stock Exchange (NYSE). <br>
<br>
Under NYSE corporate governance rules, the majority of the board members of 
listed companies are independent and audit committees must comprise of at least 
three independent directors. Furthermore, no director is considered as 
independent by the exchange unless the board of directors confirms that the 
director has no material direct or indirect relationship with the company. <br>
<br>
A director cannot normally be considered as independent by NYSE if any one of 
the following criteria exists: <br>
a. Being an employee of the listed company or any associated company (e.g. 
parent, affiliate or subsidiary). <br>
b. Having an immediate family member in the position of a CEO of the listed 
company. <br>
c. Receiving more than $ 100,000 per annum in compensation from the company. <br>
d. Being associated with internal or external audit of the company. <br>
e. Being an executive officer of another company that pays the listed company 
amounts pa that exceed $ 1 million or 2 per cent of its gross revenues. <br>
<br>
Despite good regulations, the dramatic collapse of Enron and other financial 
reporting deficiencies of some world-class companies recently has called into 
question the effectiveness of the role of directors and independent auditors.
<br>
<br>
No corporate governance regime will work without the effective participation of 
shareholders. To put it boldly, shareholders need to be more proactive and 
accountable. They should have their voices heard and concerns/queries be 
addressed properly. It is disappointing to note locally how some of the Annual 
General Meetings (AGMs) of publicly listed companies have very little 
participation from shareholders in terms of physical attendance (i.e. without 
the use of proxies) and in terms of dialogue and discussions with directors. <br>
<br>
Directors are faced with ever increasing challenges and responsibilities. A 
director may also be personally liable should there be accounting 
misrepresentation. It is, therefore, very important that they are given correct 
guidance by the relevant authorities in order to ensure that their legal duties 
are properly exercised. Oman is on the right path, but there is a long way to 
go.<br>
<br>
