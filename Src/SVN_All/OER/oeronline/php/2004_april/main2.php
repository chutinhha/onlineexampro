<p><font face="Verdana" size="4" color="#FF0000">Beat the heat</font></p>



<p align="justify"><b><font face="Verdana" size="1">The Sultanate's harsh 

summers prolonging for about 6 months of the year have spawned a flourishing 

market for air conditioners triggering an intense competition among the players. 

Janaki Raghavan speaks to the leading players to get an update on the industry.</font></b></p>



<p align="justify"><font face="Verdana" size="1">

<img border="0" src="spotlight.jpg" align="left" hspace="5" width="250" height="163">With 

a spate of new air conditioner models being launched almost daily the heat of 

the competition in the Sultanate's air-conditioner industry is rising in tandem 

with the soaring mercury levels as the country is facing what threatens to be 

yet another scorching summer typified by temperatures that rarely fall below the 

40 degrees celcius mark. No one in the industry is complaining though, for 

shooting temperatures translate into upsurge in business for this industry. &quot;The 

Sultanate's air-conditioner industry is one of the country's most vibrant and 

visible segments commanding a market size of RO. 12 million per year with almost 

100,000 units being sold annually, which is quite an impressive figure for a 

country with a population of just about 2.4 million,&quot; opines a leading dealer in 

the industry. <br>

<br>

These figures combined with a growth rate estimated to be around 10-15% mean 

that a meaty market is up-for-grabs and therefore it is hardly surprising that 

over 50 brands of air conditioners are locked in a fierce battle for claiming a 

substantial share of the pie. &quot;Despite the high levels of penetration and 

reaching a fair level of maturity the market continues to grow fuelled by 3 

major factors - the replacement market, increase in the number of households and 

use in industries or offices driven by the general industrial activities 

witnessed in the region,&quot; comments N.Pandalai, senior general manager, Bahwan 

Electronics. <br>

<br>

The industry has also been witnessing a slew of changes reflecting global trends 

in recent years-prime among which is the rapid shift from window A/Cs to split 

units so much so that this segment has been growing at a much higher rate than 

the industry as a whole according to industry pundits. The industry choruses in 

unison that this segment is the most promising one where all the action will 

take place in the coming years. &quot;Split A/Cs across the globe have grown at a 

phenomenal pace over the past few years. The rate of change is normally dictated 

by price differential between the window and split categories, new construction 

activity and replacement potential. The overall trend is true for Oman as well, 

however the peak activity in splits is yet to be seen here and expected over the 

next couple of years,&quot; opines P.V. Rao, divisional manager, EHA, Genetco. <br>

<br>

According to V. Jayachandran, assistant divisional manager, air conditioning 

division(ACD), Khimji Ramdas, &quot;The Oman market constitutes both window and split 

types of air conditioners with the window type taking about 75% of the total 

share. However, in recent years with competition becoming keener &amp; prices 

falling very steeply, the Omani customer is definitely moving towards split air 

conditioners, so-much-so that perhaps by year 2005 the ratio of window to split 

air conditioners may very well be 55 to 45. Strangely though the overall market 

for these air conditioners remains stagnant at about 100,000 units every year. 

Additionally the potential for this market, like others, will always be 

dependent on the government spending &amp; regulations. Given the trend of this in 

the current context, it is envisaged that the market could grow by at least 8% 

to 10% in the current year. As otherwise, it will only be a matter of one 

competition gaining over the other, as market data shows.&quot; <br>

<br>

&quot;A majority of the new buildings coming up have provision for installation of 

split air conditioners. Affluent villa owners are opting for low noise ducted 

units whereas package units are being phased out,&quot; reveals Varghese Samuel, 

senior manager, air conditioning division, MEC. <br>

<br>

&quot;Apart from the narrowing price differentials between the two product categories 

another key contributor to the spiralling popularity of split units is the 

growing awareness amongst the customers about the leading edge split units offer 

in terms of aesthetics, quiet cooling, lower power consumption, and the various 

value added features like special filters to deliver clean healthy air, ionisers 

for creating a refreshing atmosphere inside the air conditioned space, and also 

the various easy convenience and comfort features like powerful mode (for quick 

cooling on a hot summer afternoon), quiet mode, and personal airflow pattern 

creation, which are available at the touch of a button on the trendy and feature 

loaded remote control,&quot; declares Mukund Menon- manager, Sales Air conditioners 

division, Omasco. <br>

<br>

So how does a customer clinch his decision, with a sea of clones wooing him with 

identical features? <br>

<br>

&quot;The consumer profile is clearly bifurcated into two segments one being the 

bargain buyer who looks at only lowering the initial outlay and the other the 

value conscious buyer who lays more emphasis on the reputation of the brand and 

the value it offers in the long run in terms of lower recurring cost rather 

lower initial cost,&quot; informs Pandalai. <br>

<br>

&quot;The average Omani customer prefers Japanese makes, unless they become 

absolutely unaffordable. The reason being that Japanese makes have turned out to 

be the most efficient, most durable and almost failsafe, as opposed to other 

products. With this realisation dawning on the public slowly but surely the 

Japanese brands are gaining a wider acceptance despite Chinese makes sporting 

very low price tags,&quot; claims Shankar Naganathan, divisional manager, ACD, Khimji 

Ramdas. <br>

<br>

Whereas Samuel mentions, &quot;While villa owners opt to install premium Japanese 

brands, which could last for about 20 years, builders of rented property choose 

cheaper brands from Korea and China due to budgetary constraints. <br>

<br>

Price still is a key criterion for customers. Major Chinese brands like Gree, 

Media, &amp; Chigo have already entered the Gulf market in a very big way due to 

very attractive price.&quot; <br>

<br>

&quot;As with any other consumer durable product, customers are conscious of the 

image that the brand projects in terms of quality, reliability, easy 

availability, the price or the value for money angle. During the last few years, 

thanks to the continuous innovations in technology and the introduction of new 

products with value added features, and the benefits to customers being 

displayed, customers have begun to look for features like compact and sleek 

looks to suit the interior decor, higher cooling capacities and lower power 

consumption, low noise units, air purification system, odour removing features 

and other convenience features on the remote,&quot; says Menon. According to dealers 

the pre and after sales back up is another crucial factor that tilts the scales 

in favour of a particular brand. <br>

<br>

&quot;The prices were bound southwards in the last couple of years on account of 

competitive pressures and cost reduction initiatives undertaken by the 

manufacturing units. However they are likely to stabilize this year and the 

focus of most of the reputed brands in the coming times will be on value added 

services or features,&quot; concludes Pandalai.</font></p>



