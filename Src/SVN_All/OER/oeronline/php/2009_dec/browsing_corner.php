                      COMMANDMENTS FOR BUSINESS FAILURE<br>
                      </font> </b><font face="Verdana" size="1"><b>Reading a book 
                      by the CEO of Coca Cola, one gets an inkling on how to avoid 
                      the pitfalls in decision making, says Ganesh Sundararaman</b><br>
                      <br>
                      Let me begin by saying that this is a must-read for any 
                      business head who thinks he knows all! Donald R Keough has 
                      a fascinating style of narration. The opening pages establish 
                      that for s<img src="images/img126.jpg" width="149" height="235" hspace="9" vspace="5" align="right">ure. 
                      As he takes us through the beginnings of the thought behind 
                      the entire book, Keough’s style is so simple and pleasantly 
                      narrative, that one begins to get a bit impatient about 
                      what business thoughts he’s going to talk about in the coming 
                      chapters.<br>
                      <br>
                      That his entire illustrative is based on his experiences 
                      as the president of the Coca Cola Company is immaterial. 
                      That he has drawn upon every bit he learnt from mistakes 
                      he might have made at Coke is.<br>
                      <br>
                      His father had the guts to say “I won’t quit”. He made it 
                      as one of the most respected cattle traders of his time. 
                      But it’s interesting that Keough learnt so much from his 
                      father, just spending his spare time with him. If that demonstrates 
                      the learning processes that Keough went through, then his 
                      tenure as the president of Coke brings even the today organisations 
                      to a platform that might have been scoffed at by some gurus 
                      in the enterprise business. It would be unfair to any prospective 
                      reader of this highly interesting narrative for any reviewer 
                      to go through each of the ten commandments. To appreciate 
                      how business leaders could place their egos above the organisation 
                      is amply evident in each of the chapters. Unless one goes 
                      through each circumstance as Keough aptly describes them, 
                      it would not be possible to appreciate the difference between 
                      those who work with contemporary business decision making 
                      methods, and the ones who work with fire in their belly.<br>
                      <br>
                      Taking the correct decision when opportunity strikes, the 
                      aftermath of a CEO’s stubbornness about his hunches, it’s 
                      all brought out in many of the commandments. In many chapters, 
                      Keough is having a merry bash at a lot of contemporary thinking. 
                      But the aspects that come out at the end as sure winners 
                      in the decision making game are those that relate to calculated 
                      risks, bringing the team together and ‘listen to a lot of 
                      approaches before you insist on your own.’<br>
                      <br>
                      The end is intriguing. Keough signs off about great companies 
                      and smart people - “No matter what happens, they are never 
                      mired up in failure. They fall but always find ways to pick 
                      themselves up and move on…” <br>
                      <br>
