                    </font><b><font face="Verdana" size="2">Solar Power Is The 
                    Future Of Renewable Energy</font></b><font face="Verdana" size="1"><br>
                    <b>A recent conference on Solar Technology, highlighted the 
                    increasing need to invest in the different sources of renewable 
                    energy, particularly against the backdrop of waning conventional 
                    energy resources. Also present at the conference was HE Angelika 
                    Storz-Charkarji, the German Ambassador</b><br>
                    <br>
                    <br>
                    <img border="0" src="images/img4.jpg" align="right" hspace="8" vspace="3" width="250" height="166">	
                    Energy experts believe that there is a huge potential to exploit 
                    power from renewable energy, especially solar power. <br>
                    <br>
                    Speaking at a Solar Technology Conference, His Highness Sayyid 
                    Tarik Bin Shabib Bin Taimur Al Said stated that when he was 
                    deciding on what to say on solar energy and renewable energy 
                    in general, the first question that came to his mind was how 
                    could one get people, government and businesses excited about 
                    this subject. <br>
                    <br>
                    “I believe that talking passionately about the subject and 
                    by showing governments and businesses how it can be good for 
                    them financially, ethically and by building reputation, it 
                    would be helpful. We happen to be living in an era when green 
                    issues are in vogue. Despite the financial crisis, there is 
                    still a great level of talk and investment in green issues,” 
                    he stated. <br>
                    <br>
                    Ursula Borak from the German Federal Ministry of Economics 
                    and Technology in Oman said that so far the German government 
                    had put great emphasis to the further increase of renewable 
                    energy. <br>
                    <br>
                    “And now I can say that the new government, more than before 
                    supports renewable energy. The promotion of the use of renewable 
                    energy in Germany since 1991 has resulted in the build-up 
                    of renewable energy that offers some of the world’s leading 
                    technology,” she stated.<br>
                    <br>
                    Berthold Breid, CEO, Renewable Energy AG said that if one 
                    wanted to develop a market, it was of course important to 
                    share ideas and knowledge about innovation. “It’s also important 
                    to develop the right political framework. It is important 
                    to have companies that dare to widen their business and who 
                    take a new direction with new products and services. It is 
                    also important to get financing. You need companies, financing, 
                    innovation and political framework,” he said. </font> 
                  <p align="justify" style="margin: 3px 10px" dir="ltr"> <font face="Verdana" size="1"> 
                    <br>
                    Speaking on Germany’s statistics, Breid said that Germany 
                    generates 15 per cent of electricity out of renewable. “The 
                    amount of energy generated through wind is more in 2008, then 
                    water power. There has also been an increase in electricity 
                    out of biomass. In Germany there is lot of greenery and hence 
                    you can use biomass for electricity generation,” he stated. 
                    Elaborating on photovoltaic Breid stated that there has been 
                    increasing use of photovoltaic. “Photovoltaic is the generation 
                    of electricity through sunlight. We use solar modules for 
                    that,” Breid noted. <br>
                    <br>
                    </font><b><font face="Verdana" size="2">L&amp;T Inaugurates 
                    Sohar Manufacturing Facility</font></b><font face="Verdana" size="1"><br>
                    <b>The Indian infrastructure giant plans to generate about 
                    15 per cent of its annual revenues from the Middle-East in 
                    the next 3-4 years</b><br>
                    <br>
                    L&amp;T Heavy Engineering inaugurated a high tech manufacturing 
                    facility in Sohar, on November 16, 2009. The new unit in the 
                    Sohar Industrial Port Company (SIPC) area will augment the 
                    existing modular <img border="0" src="images/img5.jpg" align="right" hspace="8" vspace="3">fabrication 
                    facility, making it one of the largest integrated manufacturing 
                    complexes in the region catering to the hydrocarbon and power 
                    sectors.<br>
                    <br>
                    Larsen &amp; Toubro Heavy Engineering LLC is a joint venture 
                    of The Zubair Corporation and Larsen &amp; Toubro Limited, 
                    India. With this JV, L&amp;T now operates four companies in 
                    the Sultanate, expanding its operations to cover sectors including 
                    construction, erection of transmission towers, modular fabrication 
                    and manufacture of critical equipment for process plants. 
                    The Sohar facility is a pioneering venture in the GCC region 
                    and L&amp;T’s maiden venture in critical fabrication projects 
                    outside India. The facility has been constructed by L&amp;T 
                    Oman LLC. Construction was completed in a record time and 
                    trial production has commenced.<br>
                    <br>
                    <font color="#CC3333"><b>State-of-the-art </b></font><br>
                    A succession of spectacular load outs in 2009, along with 
                    total revamp of Hurricane (RITA) damaged jack up rig FD-VII 
                    has affirmed its capabilities as a world class venture with 
                    a potential to launch the Sultanate as a major player in offshore 
                    fabrication industry. The yard is now fully geared up for 
                    fabrication &amp; load out of a eight legged jacket weighing 
                    a colossal 10,000 metric tons. The jacket will be rolled on 
                    to a custom built barge for onward transportation to Mumbai 
                    High North offshore field of Oil &amp; Natural Gas Commission 
                    (ONGC)<br>
                    <br>
                    The facility is spread over 300,000 sq metres and has a built-up 
                    area of 12,000 sq mtrs with an additional 100,000 sq mtrs 
                    available for future expansion. Since the facility is built 
                    on the sea front, extensive piling had to be done before the 
                    structure could be erected. The facility has been qualified 
                    by the American Society of Mechanical Engineers for manufacture 
                    of code stamped equipment. It has also secured ISO certifications. 
                    Says A M Naik, Chairman and Managing director L&amp;T, “We 
                    plan to generate about 15 per cent of our annual revenues 
                    from the Middle-East in the next 3-4 years from the present 
                    10 per cent as the company scales up its presence in the region.” 
                    The company has a presence in Kuwait, United Arab Emirates 
                    and Saudi Arabia, among others and, subject to favourable 
                    conditions, may look at Iraq and even Yemen. The company, 
                    which has 15 divisions overall, generates about $800-850mn 
                    from the region, out of its total revenues of $8.5bn. “We 
                    plan to increase our footprint in the region manifold.” <br>
                    The company is committed to empower local people through employment 
                    generation. <br>
                    <br>
                    </font><b><font face="Verdana" size="2">Opening New Vistas</font></b><font face="Verdana" size="1"><br>
                    <b>The eGovernment Services Portal was launched on November 
                    8, 2009</b><br>
                    <br>
                    <img border="0" src="images/img6.jpg" align="right" hspace="8" vspace="6"> 
                    The official eGovernment Services portal, the nation’s flagship 
                    achievement, was launched on November 8, 2009, in the Garden 
                    Area at the Al Bustan Palace Hotel, under the umbrella of 
                    e.oman, Oman’s national IT Strategy. The launch was held under 
                    the patronage of HH Sayyid Haitham bin Tariq Al Said and was 
                    attended by HE Mohammed bin Nasser Al Khusaibi and board members 
                    of the Information Technology Authority (ITA); Dr Salem bin 
                    Sultan Al Ruzaiqi, ITA’s CEO and other distinguished guests. 
                    <br>
                    <br>
                    The Sultanate of Oman has embarked upon an ambitious journey 
                    in transforming Oman by empowering its people, through the 
                    e.oman Initiative, within the digital society. The goal includes 
                    a significant improvement in the quality of services the government 
                    provides to its citizens. It aims to create an effective government-community-citizen 
                    infrastructure that provides better public services to people. 
                    HE Khusaibi said, “The progressive development of eGovernment 
                    services bears witness to the aspirations, ambition and effort 
                    put forth to secure the desired national objective. This is 
                    especially relevant, as information technology has become 
                    the driving force behind economic advancement.”<br>
                    <br>
                    As the main entry point to the Government system and its services, 
                    the Official eGovernment Services Portal, <a href="http://www.oman.om">www.oman.om</a> 
                    , caters to all individuals in the Sultanate of Oman, and 
                    bridges the gap between government bodies and citizens, residents, 
                    visitors and the business sector. A key benefit of the portal, 
                    is that it can be accessed by anyone, from anywhere, and at 
                    anytime. Aiding in the transformation of Oman into a sustainable 
                    Knowledge Society, the Official eGovernment Services Portal 
                    uses digital technologies to improve the efficiency of internal 
                    processes and is designed to serve the public quickly, effectively, 
                    and in an interactive manner.<br>
                    <br>
                    <font color="#CC3333"><b>Customer focus</b></font><br>
                    This <img border="0" src="images/img7.jpg" align="left" hspace="8" vspace="6">bilingual 
                    portal is designed on a customer-centric model, as it contents 
                    are structured by the daily events of life so that online 
                    visitors can easily access the services that are most relevant 
                    to them. Through a multi-channel service delivery system, 
                    navigation is simplified to enable easy access to services 
                    within the portal. The portal also authenticates entry to 
                    other government services and permits access to their integrated 
                    services. <br>
                    <br>
                    The portal is of use to both users and government entities 
                    alike. Users benefit by being able to access a number of government 
                    agencies at their convenience. Users have access to contact 
                    information, service procedures, processing fees, online payment 
                    options, and electronic forms to request for services within 
                    just a few clicks and can themselves keep track of their request 
                    status. On the other hand, government entities can share the 
                    user-related information and also certain common applications 
                    such as authentication and online payments. Integrated service 
                    delivery through a single window is possible for all government 
                    entities that are connected to the government network which 
                    is the fundamental backbone of the portal. <br>
