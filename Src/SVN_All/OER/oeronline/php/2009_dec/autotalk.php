                    <br>
                    </font></b><font face="Verdana" size="1">There are a number 
                    of things that you can criticize a Porsche 911 for. The car 
                    may seem to be a bargain at first but don’t get too comfortable. 
                    Because while <img src="images/img115.jpg" hspace="8" vspace="5" border="0" align="right" style="border: 1px solid #E9E9E9">the 
                    car comes with decent equipment levels from the get go, a 
                    lot of kit that comes as standard on most cars of a similar 
                    price comes in as an extra cost options on the 911 and prices 
                    tend to go skyward fast. Moreover, maintaining a car such 
                    as this doesn’t come cheap either, but I guess most of you 
                    already knew that.<br>
                    <br>
                    Then there are the back seats which, like in most 2+2 cars, 
                    are there for one of three reasons - either to seat very small 
                    children, to use as an extra luggage space or most likely 
                    to justify the car to your spouse as a four seater so that 
                    they would allow you to purchase it. There is also some wind 
                    noise present and suspension thud on rough surfaces and if 
                    you are used to regular paddle shifters on most cars the PDK 
                    steering shifters can take quite some getting used to. But 
                    the fact of the matter is, we really don’t care about any 
                    of this because the 911 is by far one of the best cars you 
                    will ever drive and for that we will forgive it for everything, 
                    except maybe for the odd steering mounted shifters.<br>
                    <br>
                    The car that we drove was a Nordic Gold Metallic 911 Targa 
                    4S equipped with the new Porsche Doppelkupplung (PDK) gearbox, 
                    a 7-speed dual clutch system, and the Sport Chrono Package 
                    Plus (well get to this later in the review). From the outside 
                    the new car sticks with the 911’s tried and true design with 
                    not many changes between the older and newer version but the 
                    incredible combination of the colour and the Targa’s glass 
                    roof and the 44mm wider stance was all the car needed to stand 
                    out from the regular crowd, believe us when we say that this 
                    is one breathtaking car to look at.<br>
                    <br>
                    Inside, it is regular Porsche fare. Anyone who has used a 
                    Porsche before will be perfectly at home in the new car as 
                    well and for the remainder of us who haven’t it is not really 
                    that hard to get used to, just remember that the starter keyhole 
                    is on the left-hand side of the steering column like in every 
                    other Porsche. As for the seating itself, there is only one 
                    way to describe it – sheer perfection. The seats are comfortable, 
                    even for long journeys, the driving position is perfect and 
                    there is more than enough lateral support during corners, 
                    even with the standard seats. The dash itself is a great combination 
                    of simplicity and sportiness, especially with the rev counter 
                    taking the centre stage. The rest of the controls are decently 
                    laid out and as you would expect from a German car the materials 
                    used are top-notch.<br>
                    <br>
                    On the road the P<img src="images/img116.jpg" hspace="8" vspace="5" border="0" align="left" style="border: 1px solid #E9E9E9">orsche 
                    is probably one of the best tools you can have at your disposal 
                    to have fun in. Just beyond the rear axel sits a thunderous 
                    3.8l flat-6 that punches out 385hp@6500rpm and 420Nm@4,400rpm. 
                    Considering the load on the rear wheels during takeoff, the 
                    grip is immense. You will see 100kmph in just 4.9s and 160kmph 
                    in just 10.3s. But as we mentioned before the 911 we were 
                    driving was equipped with the Sport Chrono Package plus and 
                    Porsche’s PDK gearbox. Porsches new gearbox works in a similar 
                    fashion to the system found in VW and Audi but has been tuned 
                    and enhanced to shift gears as fast as is mechanically possible. 
                    The shifts are seamless and incredibly smooth but it is quite 
                    surprising why Porsche did not stick with the norm for the 
                    steering mounted shifters, it would have made the experience 
                    much more engaging than it already was. <br>
                    <br>
                    Depress the Sport button and the engine management system 
                    enhances engine and throttle response, the steering becomes 
                    more direct, the damping becomes harder and the gear shifts 
                    become shorter. And then if you plan on going completely bonkers 
                    there is the ‘Sport Plus’ button! Engage this and Launch Control 
                    is made available to you and shift times on the PDK are improved 
                    to be the fastest they can possibly be while at the same time 
                    optimising shift points. <br>
                    <br>
                    Combine the PDK and the Sport Plus package and the entire 
                    car changes - the 0-100kmph and 0-160kmph times are reduced 
                    significantly to 4.3s and 9.4s respectively and the handling 
                    reaches a level that is its own. Combine this with the incredible 
                    exhaust note and stunning feel of the steering and you end 
                    up with one of the most exhilarating cars you will ever drive. 
                    We have never seen a car that strikes such a balance between 
                    electronic assistance and driver control, it is just an incredible 
