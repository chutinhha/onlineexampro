                    services and dial-up Internet access of at least 28 kilobytes 
                    per second in the beginning. The successful bidders would 
                    be provided a licence through the govern</font><font size="1" face="Verdana, Arial, Helvetica, sans-serif">ment’s 
                    Universal Service Obligation (USO) initiative and they are 
                    expected to roll out services 18 months after the award of 
