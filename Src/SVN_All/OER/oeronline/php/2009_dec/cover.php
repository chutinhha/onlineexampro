                    <font color="#CC3333"><b>Pelletisation plant</b></font><br>
                    The foundation stone for Brazilian conglomerate giant Vale’s 
                    iron ore pelletisation plant was laid in March at the Sohar 
                    Industrial Port Area. The $350mn project includes building 
                    the iron ore pellets plant with a production capacity of 9 
                    million tons per annum. It also includes a distribution centre 
                    with handling capacity of 40 million tons per annum at the 
                    Sohar Industrial Port.<br>
