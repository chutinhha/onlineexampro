                    <b><font color="#CC3333">Implementation of FTA with US</font></b><br>
                    The Oman-US Free Trade Agreement (FTA), which came into force 
                    on January 1, 2009 has opened up a plethora of opportunities 
                    for traders in Oman and the United States. With the implementation 
                    of the agreement, consumers in Oman can</font><font size="1" face="Verdana, Arial, Helvetica, sans-serif"> 
                    expect quality US products to become cheaper, especially in 
                    the automobile, electronic and IT-related sectors. <br>
                    <br>
                    In today’s challenging global environment, the FTA provides 
                    new opportunities and expanded horizons for economic development 
                    to Omani and to American businesses alike. The liberalisation 
                    will expand opportunities for exports of machinery, automobiles, 
                    optic and medical instruments, electrical machinery, and agricultural 
                    products such as vegetable oils, sugars, sweeteners and beverage 
                    bases. <br>
                    <br>
                    <font color="#CC3333"><b>Renewable energy</b></font><br>
                    The Authority for Electricity Regulation (AER) established 
                    in 2004 to regulate the electricity and related water sector 
                    appointed consultants to assist, review and approve the Sultanate’s 
                    first Renewable Energy Pilot Projects during the first half 
                    of this year. The consultancy firm, COWI, will assist with 
                    reviews and approval of renewable energy pilot projects, which 
                    have been submitted</font><font size="1" face="Verdana, Arial, Helvetica, sans-serif"> 
                    by local and international investors following the publication 
                    of “Renewable Energy Study” in 2008. <br>
                    <br>
                    The study identified significant solar and wind energy potential 
                    in the Sultanate and recommended early implementation of small 
                    scale renewable energy pilot projects, initially in rural 
                    areas, to confirm the performance and efficiency of renewable 
                    technologies in local conditions. These pilot projects will 
                    be implemented in cooperation with Rural Areas Electricity 
                    Company.<br>
                    <br>
                    <b><font color="#CC3333">Market stabilisation fund</font></b><br>
                    Oman’s Market Stabilisation Fund, set up with a capital of 
                    150mn rials ($389.6mn) came into operation in February 2009. 
                    The fund started investing in local shares to stabilise the 
                    Muscat Securities Market, which had taken a hit reflecting 
                    the tre</font><font size="1" face="Verdana, Arial, Helvetica, sans-serif">nds 
                    in the rest of the stock markets of the world during the 2008-2009 
                    financial crisis. The fund, which was created in November 
                    2008 and started operating in Februaury 2009, is 60 per cent 
                    owned by the government.<br>
