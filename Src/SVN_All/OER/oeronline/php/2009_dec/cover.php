                    would be a pilot project to cover around 118 settlements. 
                    <br>
                    <br>
                    There are around 2000 settlements with a population of more 
                    than 150,000 in the Sultanate without fixed-line telephones. 
                    The south Al Batinah region has been selected as the first 
                    area for the pilot project. The USO initiative is part of 
                    the government’s move to liberalise the telecom sector of 
                    Oman. <br>
