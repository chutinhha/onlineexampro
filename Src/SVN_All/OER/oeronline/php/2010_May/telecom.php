		        <td valign="top" width="100%"> 
				<p align="center" style="margin: 3px 10px"> 
					</p>
				<p align="justify" style="margin: 3px 10px"> 
					<b><font face="Verdana" size="2">Telecom companies in Oman 
					can no longer dismiss VoIP as an illegal service but instead 
					have to find ways to offer customer convenience, writes 
					Visvas Paul D Karra<br>
					<br>
&nbsp;</font></b></p>
				<p align="justify" style="margin: 3px 10px"> 
					<b><font face="Verdana" size="4" color="#C00000">
				<img border="0" src="images/may-telecom01.jpg" hspace="9" vspace="5" style="border: 1px solid #C0C0C0; " width="120" height="154" align="right"></font></b><font face="Verdana" size="1">The 
					term Voice over Internet Protocol (VoIP) or internet 
					telephony denotes a transmission technology for delivery of 
					voice communications over the Internet. When VoIP service 
					was introduced over broadband, Internet users were able to 
					make and receive calls as they would over a normal telephone 
					(PSTN).<br>
					<br>
					The popularity of VoIP grew because it afforded cheap 
					international calls and PSTN users switched to VoIP in 
					increasing numbers. Consequently, authorities in a number of 
					countries where telecommunications was a state monopoly 
					began to impose restrictions on the use of VoIP as 
					telecommunication companies began to experience reduced 
					traffic. Suddenly, the term VoIP became an illegal thing. 
					However, there are a number of dimensions to the use of VoIP 
					and how it has a broader definition than just being an 
					illegal international phone service being provided by 
					unauthorised cyber cafes.<br>
					<br>
					In Oman, the state-owned Oman Telecommunications (Omantel) 
					Company which till date has the only international gateway 
					discovered that the international traffic was going down 
					even though there was no decrease in the number of 
					population, chiefly the Indian sub-continent expatriates who 
					make the bulk of international calls. The main cause was 
					that 80 per cent of international telephone calls were going 
					through VoIP, which in Oman’s case, was offered by cyber 
					cafes who were not licensed to provide international 
					telephone services.<br>
					<br>
					<b>Cheap alternative<br>
					</b>The reasons for this are easy to find. The illegal VoIP 
					calls to India, for example, used to range between 25bz to 
					50bz per minute any time of the day in comparison to the 
					regular and legal calls which ranged anything from 75-99bz 
					(off peak) to 175-200bz (peak). With such a huge price cut, 
					people were willing to compromise on the voice quality.<br>
					<br>
					According to Rao K Chagarlamudi, VP Consumer Business Unit, 
					Omantel, customers who want to make international calls 
					always look for cheap rates and this is why the illegal VoIP 
					service providers were booming. But the cybercafés were not 
					licensed to offer telecom services, and therefore the 
					authorities promptly clamped down on them in November last 
					year with the help of Royal Oman Police (ROP).<br>
					<br>
					It is altogether another story that the people found other 
					means to make their ‘low-cost’ calls, but the fact remains 
					that Oman is only following what many other countries are 
					already doing to protect the telecom monopoly.<br>
					<br>
					Antti Arponen, CEO, FRiENDi Mobile opines that when a big 
					part of any country’s telecom revenues comes from calls made 
					to international destinations and there is a monopoly it is 
					very natural to have that status quo maintained for as long 
					as possible. One way of doing that is by blocking VoIP 
					services. <br>
					<br>
					FRiENDi Mobile, which is a Class II licence operator using 
					the network of Omantel’s subsidiary Oman Mobile, has a 
					number of products that cater exclusively to the people in 
					need of international calling. For example, with the promise 
					of “Lowest International Call Rates”, and international call 
					prices as low as 69bz per minute, FRiENDi Mobile is the 
					closest to match the VoIP calls.<br>
					<br>
					The other pressing issue at hand for the authorities was 
					related to security concerns as VoIP calls went through 
					internet data lines and could not be supervised. “Monitoring 
					the voice traffic over internet is more difficult than 
					tracking the voice calls. This may have contributed to 
					authorities deciding to stop VoIP through cybercafés,” adds 
					Arponen.<br>
					<br>
					<b>Get the customer back<br>
					</b>And since customers are increasingly turning to the 
					internet to find alternate ways to make their calls, telecom 
					companies are being forced to do a rethink on their 
					strategies. The first off the block is Omantel, which plans 
					to introduce full-fledged VoIP services, which can be used 
					with the regular mobile handsets.<br>
					<br>
					“We are trying to fulfil our customers’ needs, especially 
					those from India, Pakistan, Bangladesh, Sri Lanka and 
					Philippines who want to call their loved ones and are simply 
					trying to maximise value for their money. We are going to 
					come up with a service that offers convenience and at a 
					reasonable price,” says Rao, highlighting what Omantel 
					intends to offer to its customers.<br>
					<br>
					“Reasonable quality of voice, reasonable price and 
					reasonable convenience will be the three pillars of the new 
					service. But it will not be like our premium service and it 
					may be limited to certain international destinations. Our 
					aim is to win back our customers who were using the illegal 
					VoIP services and that is why we will be launching it on the 
					mobile handsets to provide convenience to them,” adds Rao.<br>
					<br>
					Talking about the pricing factor, Rao says that it will not 
					be exactly as cheap as the rates offered by the illegal 
					internet café operators because as an incumbent telecom 
					operator, they have to pay royalties to the Government, 
					charges to foreign operators where the calls are ending 
					unlike the cyber cafes, which had no such responsibility.<br>
					<br>
					<b>Opening up<br>
					</b>Market experts, however, feel that blocking of VoIP 
					services provided by cyber cafes and trying to win back 
					customers is a wrong way of trying to sort out the problem. 
					One better way of doing it is to open up the competition and 
					allow all the existing telecom companies to negotiate their 
					own international agreements, which would bring down the 
					call rates.<br>
					<br>
					If the international traffic is opened to competition, that 
					will make everybody select the best technology to route 
					their traffic and find the most cost efficient way of doing 
					it. Inevitably, the prices will go down and ultimately the 
					companies will benefit because the consumers will use the 
					regular telecom services doing away with the necessity of 
					VoIP services altogether. The regulator will also be happy 
					because they will not have security issues.<br>
					<br>
					From a consumer’s perspective, if there is a cheaper way of 
					being in touch with families and friends back home and that 
					facility is blocked, they feel deprived. All telecom 
					companies should try to bring down the cost of their 
					international calls as far as possible without breaking 
					rules and regulations, emphasises Arponen.<br>
					<br>
					“We at FRiENDi Mobile are going out of our way to offer 
					international calls with low rates. We have tied up with 
					IDEA mobile company in Kerala in India and Dialog Telekom in 
					Sri Lanka, as well as Globe in Philippines, to provide more 
					benefits to our subscribers. This way, FRiENDi Mobile has 
					been able to offer a more convenient, similarly priced, much 
					higher quality, and a 100% legal way to make international 
					calls, compared to illegal internet traffic,” Arponen points 
					out.<br>
					<br>
					</font><font face="Verdana" size="1"><b>Future is VoIP<br>
					</b></font> 
					<b><font face="Verdana" size="4" color="#C00000">
				<img border="0" src="images/may-telecom02.jpg" hspace="9" vspace="5" style="border: 1px solid #C0C0C0; " width="120" height="161" align="right"></font></b><font face="Verdana" size="1">Cracking 
					down on illegal VoIP service providers and introducing VoIP 
					on mobile phones are all the visible aspects but there are 
					invisible factors of VoIP, which need to be considered as 
					well. Because, technology has advanced to such an extent 
					that VoIP is no longer just an illegal call making facility 
					but it is a technology that directs a voice call through a 
					PC to the end user. With Internet speeds becoming faster, 
					transferring an international call via VoIP is usually cost 
					effective than old school telephony.<br>
					<br>
					In fact, it is said, but not proven, that many a regular 
					legal calls from a mobile handset could in all probability 
					be routed through an internet data cable to UK or the US and 
					a consumer may not be able to notice it. The voice quality 
					will be bad or good depending upon the telecom operator’s 
					pricing strategies and the infrastructure. They could use 
					VoIP and pocket the revenue margins.<br>
					<br>
					Nawras, the second Class I mobile operator in the country, 
					wants to ensure complete transparency with the issue of VoIP 
					when it launches its fixed line services. Giving details of 
					this, Tore Solberg, chief commercial officer, Nawras says 
					that because of the way they are building their fixed 
					network, its fixed line national services will be running on 
					VoIP technology with the help of Wimax. Subscribers of 
					Nawras fixed line network will, basically, be using VoIP 
					technology to make calls.<br>
					<br>
					“This will give us a possibility to offer our customers 
					subscription-based calling products. Presently, you pay per 
					minute or call but with Nawras fixed line services, the 
					customer will get a subscription package to call any other 
					Nawras fixed line subscriber for free the whole month for a 
					certain sum of money,” informs Solberg.<br>
					<br>
					Then the customer will actually be paying to access the 
					network which will be Wimax based on VoIP technology. 
					Eventually, a customer will also get a choice of making 
					international calls from a fixed line using either the PSTN 
					or VoIP technology both from the same fixed line phone by 
					using a prefix number. When you make a regular international 
					call, normal tariffs will apply and you will have your 
					high-quality voice as it will be a PSTN call. But if you 
					want to take the risk and make low-cost calls, it will be 
					routed through VoIP, Solberg explains.<br>
					<br>
					Since the time the industrial revolution began with the 
					invention of the steam engine, technology has been used as a 
					tool to usher convenience to mankind. In that light, any 
					telecom company that implements technology as a solution to 
					its customers’ needs will be the winner. </font></p>
