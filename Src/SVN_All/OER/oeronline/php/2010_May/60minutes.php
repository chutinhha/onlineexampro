			<p align="justify" style="margin-left: 10px; margin-right: 10px" dir="ltr"><font face="Verdana" size="1"> 
                <br>
                <br>
                </font><b><font face="Verdana" size="4" color="#CC3333"> 
                Partnering for progress</font></b><font size="1" face="Verdana, Arial, Helvetica, sans-serif"><br>
			<br>
                </font><b><font face="Verdana" size="2">Sayyida Rawan Ahmed 
			Al-Said, Board Member and group CEO, ONIC Holding shares her views 
			on the company’s partnership with RSA and the prospects of the 
			insurance sector in Oman in an interview with Mayank Singh<br>
			</font><font size="1" face="Verdana, Arial, Helvetica, sans-serif"><br>
                </font>
			</b><font size="1" face="Verdana, Arial, Helvetica, sans-serif"><br>
                <b> 
                    <img src="images/may-60mints01.jpg" hspace="9" vspace="5" border="0" align="right" width="100" height="131" style="border: 3px solid #EFEFE9; ">RSA 
			Insurance Group and ONIC Holding recently entered into an agreement 
			in accordance with which RSA Oman acquired Al Ahlia from ONIC 
			Holding. Can you give us details of this partnership?
			</b><br>
			The strategic partnership with RSA will further consolidate our 
			leadership position in the insurance sector in Oman. The combined Al 
			Ahlia Insurance Company (AAICO)-RSA Oman entity will be the largest 
			insurance player in Oman in terms of net written premium. ONICH with 
			a sizable 20.03 per cent stake in the combined entity stands to 
			benefit as well. The fact that a reputed international player like 
			RSA has tied up with ONICH is a testimony to our strength in this 
			sector. We value the international repute and the best practices of 
			RSA and hope to adopt the same in ONICH. This will be a major 
			contributor to the development and growth of the insurance sector in 
			Oman. It also gives ONICH the opportunity to grow outside Oman by 
			building a strong relationship with RSA.
                <b> 
                    <br>
			<br>
			What was the idea behind this partnership? You could have gone in 
			for an outright sale but did not? Could you explain why?</b><br>
			This is indeed a unique transaction. You are correct in saying that 
			this is not a sale, it is a strategic partnership. If you look at 
			the general insurance market in Oman over the years, there have been 
			a number of new entrants and the market has been very price 
			sensitive. We think that there is a need for market consolidation 
			and consolidation need not necessarily mean sale. I am confident 
			that going forward this combined entity will create a world class 
			insurance company in Oman with RSA bringing in its international 
			best practices coupled with AAICO’s strong brand, customer relations 
			and knowledge of the local market, all of which are key ingredient 
			to success. ONICH considers insurance as its core sector and 
			believes there are growth opportunities both within Oman and the 
			region. Acquiring a 20.03 per cent stake in the combined entity is 
			an indication of this belief and commitment to the growth of this 
			sector.<br>
			<br>
			<b>How did ONIC Holding manage to get a good value in such difficult 
			times? Also the economies worldwide have seen a downturn in the last 
			two years? So what motivated you to have this merger at this 
			juncture?<br>
			</b>We were looking for a partner who was conservative and who would 
			take a long term view on the investment as we did. As I understand, 
			RSA Oman was also looking for a good general insurance business 
			which would add value and help them grow in these markets, and AAICO 
			was a perfect fit. While the negotiations were tough as in most 
			deals of this nature and size, we are extremely satisfied with the 
			outcome. I believe it’s a win-win situation for both RSA Oman and 
			us. The insurance market in Oman has about 21 players. We have been 
			the leaders in the market for a while now and felt that 
			consolidation was the key to retain our leadership position. An 
			association with a reputed and insurance focused name like RSA Oman 
			makes sense.<br>
			<b><br>
			Why do we not see more joint ventures in Oman as opposed to the 
			West, where it is a more common phenomenon? <br>
			</b>It will probably not be right to say that Oman has not seen 
			joint ventures. Oman has witnessed a number of joint ventures. I 
			however agree with you that this has probably not happened in the 
			insurance sector. I believe, we being the market leaders are setting 
			new trends and pioneering this concept in the insurance sector.<br>
			<br>
			<b>What is the new direction for Al Ahlia? <br>
			</b>We will work with RSA Oman closely once the transaction is 
			complete. Until such time RSA Oman and Al-Ahlia will continue to 
			operate as they have been in the past, it will be business as usual. 
			We see enormous synergies between the two companies going forward 
			given the brand name, network, management and the Omani workforce of 
			Al Ahlia. We are confident that the organisation will, as in the 
			past set benchmarks in the insurance sector. As I had mentioned 
			earlier, this strategic partnership is a perfect blend of local 
			expertise and international <br>
			best practices.<br>
			<br>
			<b>Post the RSA Oman deal, what do you propose to do with the money? 
			Do you plan to re-invest it?<br>
			</b>Besides the approximate RO6.5mn that we will pay for acquiring 
			the 20.03 per cent stake in RSA Oman, we are evaluating suitable 
			investment opportunities in the insurance sector in the region and 
			outside to add to our sustainable income. A part of the cash will be 
			used to strengthen our existing businesses and for repayment of 
			existing debts. All these measures will further enhance and strength 
			ONICH’s balance sheet whilst providing a growth platform for the 
			future.<br>
			<br>
			<b>You have been chosen as a Board Member of ONICH recently. What 
			are your first reactions?<br>
			</b>I thank all the stakeholders of the company for reposing their 
			faith in me and giving me the opportunity to represent them on the 
			Board, in addition to my existing role as group CEO of ONICH 
			Holding. While it means additional responsibilities, it further 
			strengthens my resolve to implement the long term strategies of the 
			company and take it to greater heights.<br>
			<br>
			<b>Can you tell us about ONIC Holding’s group strategy for 2010?</b><br>
			ONICH’s strategy is to retain its position as a premier holding 
			company with core focus on the insurance sector and supplemented by 
			its market investments. We will continue to enhance our sustainable 
			income streams by growing organically and inorganically 
			(acquisitions) in core areas. This according to us is the most 
			suitable approach to retain and enrich stakeholders value and 
			mitigate earnings volatility. Other market investment opportunities 
			will be continuously explored as per their merit.<br>
			<br>
			<b>Are there any plan to further increase your investments in 
			overseas companies?<br>
			</b>As an investment company, we are always on the lookout for 
			suitable opportunities offering us value in core sectors. The MENA 
			and the GCC regions predominantly remain our target markets with a 
			focus on insurance. Given our base in Oman, we consider ourselves 
			better positioned in terms of business relations and understanding 
			of markets, to benefit from the growth in this region. We will 
			continue to seek opportunities to expand and grow strategically.<br>
			<br>
			<b>Do you leverage to invest? <br>
			</b>ONICH only leverages for strategic acquisitions and not for 
			market investments. The company has always had a very prudent 
			leverage policy and maintained its leverage at manageable levels 
			(debt: equity of less than 0.5 per cent). This has helped us during 
			the challenging market conditions during 2008 and 2009, when a 
			number of peer companies in the region faced serious problems 
			related to debt. We will continue using these policies in the future 
			as well.<br>
			<br>
			<b>How do you anticipate the growth in investment revenue for 2010?<br>
			</b>We had a very good year in 2009 despite the challenging times, 
			largely due to our efficient asset allocation and risk measures. The 
			inherent nature of the stock markets makes it a difficult task to 
			forecast accurately, more so given the lingering overhang of the 
			financial crisis. Given the fact that oil prices are above the $70 
			per barrel range, we feel that the GCC economies should witness 
			continued growth momentum. Investment expenditure and government 
			support will be the key drivers for our markets. Moreover Oman 
			offers political and economic stability with relatively strong 
			macroeconomic fundamentals, thus enabling sustainable growth. We 
			feel the markets would reflect these factors and should be positive 
			for the rest of the year in turn having a positive impact on 
			earnings.<br>
			<br>
			<b>How are your other associates performing?<br>
			</b>Our major exposure is within the GCC and predominantly in the 
			insurance and financial services. Given that the region has been 
			affected to a lesser extent in the global financial crisis of 2008, 
			most of our affiliates have performed well despite the extremely 
			challenging times. Core operations in most of the companies in Oman 
			and outside have been stable. <br>
			<br>
			<b>Why is ONIC Holding so unique?<br>
			</b>ONICH prides in being a premier holding company with a niche in 
			the Insurance business. Our insurance subsidiaries are market 
			leaders and the fact that a reputed international player like RSA 
			has tied up with ONICH is a testimony to our strength in this 
			sector. We are a dynamic organisation which believes in adjusting to 
			the changing market conditions and that has always been our 
			strength. This has turn has helped all our stakeholders benefit over 
			the long term.<br>
			<br>
			<b>What is the key strength of ONIC Holding?<br>
			</b>Insurance<br>
			In house technical skills in insurance and investment activity<br>
			Corporate governance<br>
			Brand image<br>
			Sustained track record of creating value for all stakeholders<br>
			<br>
			<b>What does the AAICO RSA transaction mean for your other 
			subsidiary National Life &amp; General (NLG)?<br>
			</b>NLG will focus on the life and medical insurance business which 
			is its core strength. As a market leader with over 60 per cent 
			market share in life business in Oman and given the relatively low 
			penetration levels in this segment, we see immense growth potential 
			for NLG.<br>
			<br>
			<b>Will its growth trajectory be impacted?<br>
			</b>NLG’s life business is profitable and has the potential to drive 
			growth given the relative under-penetration in Oman. The general 
			business was relatively new and not generating profits. As a result, 
			we did not see an adverse impact on the growth or bottom line of 
			NLG.<br>
			<br>
			<b>What are the future plans for NLG?<br>
			</b>NLG will be a key driver of growth for our insurance business. 
			Besides focusing on life and medical insurance business, we would 
			also aggressively look to expand its operations outside Oman.
