						<p align="justify" style="margin-left: 10px; margin-right: 10px" dir="ltr"><b><font face="Verdana" size="1">
						<br>
						The</font><font face="Verdana" size="2">
						</font><font face="Verdana" size="1">bond</font></b><font face="Verdana" size="1"><b> 
						market, also known as debt market or fixed income 
						securities market, provides an important source of 
						funding for government and the corporate sector across 
						the world. However, it is in various stages of 
						development in different economies - countries such as 
						Japan, Korea and US have a robust bond market whereas in 
						GCC countries it is still in a developing stage. Oman 
						though is yet to witness the growth of a vibrant fixed 
						income securities market. </b></font></td>
						<td width="126" valign="top">
						<p align="center"><b> 
                    <font size="1" face="Verdana, Arial, Helvetica, sans-serif"> 
                    <img src="images/may-GANESH.jpg" hspace="14" vspace="9" border="0" width="80" height="86" style="border: 3px solid #EFEFE9; "><br>
						</font></b>
