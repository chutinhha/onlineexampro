				<p align="justify" style="margin-left: 10px; margin-right: 10px" dir="ltr"> 
                    <font face="Verdana" size="1">The issue of bonds by 
					corporates in the Sultanate of Oman is regulated by the 
					Capital Market Authority (CMA). Any company that is planning 
					for a bond issue needs to apply to CMA for approval along 
					with the necessary information as prescribed under the 
					regulations. Further any bond issue of more than 24 months 
					maturity requires compulsory credit rating (also applicable 
					for unsecured bond issue of more than 12 months). If the 
					bond issue size is more than RO10mn, rating by at least two 
					credit rating agencies is mandatory. All issues of bonds of 
					maturity exceeding 24 months needs to be secured by company 
					assets and a trustee needs to be appointed within six months 
					of the issue of the bonds. The CMA regulations are not 
					restrictive in nature and are in line with international 
					practices on bond issues with a view to develop a healthy 
					market. <br>
					<b><br>
					Bonds are forever!<br>
					</b>I was always fascinated by James Bond movies for its 
					high tech gadgets and the powerful screen dialogues he 
					delivers like this one – “You can’t hold the Bond for ever.” 
					But when it comes to the bond market in Oman, it doesn’t 
					hold true! <br>
					<br>
					There is enough demand for bonds from banks, pension funds 
					(PFs), mutual funds (MFs), insurance companies and high net 
					worth individuals (HNIs). Banks can invest up to 10 per cent 
					of their net worth in fixed income securities under Central 
					Bank of Oman (CBO) regulations. For all the seven local 
					banks in Oman, the total amount which can be invested in 
					fixed income securities (based on their net worth as on 
					December 31, 2009) works out to RO167mn approximately. 
					Against this backdrop, the total value of bonds listed in 
					MSM (excluding bank bonds and government bonds) is less than 
					five million rials. Since the investments in these bonds by 
					banks are not considered for the calculation of their 
					lending ratio (LDR), it releases more bank credit for 
					lending to mid-level corporates and the SME sector, thus 
					reducing the crowding out of bank credit by the so called 
					“Big Boys”. After the Global meltdown and the crisis in some 
					of our neighbouring countries, pension funds with large 
					resources at their disposal are also looking for more 
					investment opportunities within the Sultanate. Considering 
					the political stability of the country and its currency 
					being pegged to US dollars, investors from other GCC 
					countries would also be keen to invest in the bond market in 
					Oman.<br>
					<br>
					Now let us have a look at the supply side story. Oman 
					government development bonds (GDBs) have been issued in the 
					past with the latest being the 36th issue of GDBs issued on 
					August 6, 2009, for a maturity period of three years with a 
					coupon rate of four per cent p.a. These will mature on 
					September 6, 2012. The demand for these bonds was so much 
					that the final yield on competitive bids worked out to 
					around two per cent. However, the number of corporate bond 
					issues have been far and few. Since 2007, there were only 
					four corporate bond issues in the market. <br>
					<br>
					As per the CMA website, the number of bonds listed on the 
					MSM as on date is only 10 out of which government bonds are 
					four with a total value of RO252mn and corporate bonds 
					(including Banks) are six with a total value of RO172mn. 
					However, hardly any secondary market trading happens in 
					these bonds since the existing bond holders are not willing 
					to sell for want of alternate investment opportunities. In 
					other words, the bondholders are wedded to their bonds at 
					least until maturity, if not forever. <br>
					<br>
					<b>An encouraging trend<br>
					</b>In 2010, if the market reports are to be believed, many 
					corporates, both in the private sector and in the public 
					sector, are planning to raise huge long-term debt in the 
					form of project loans or long-term loans from the banking 
					system. The issuance of bonds need to be encouraged for 
					raising at least a portion of these long term debts since 
					bonds can be a successful source of funding for corporates, 
					which can be clearly seen from the Indian experience. By 
					issuing a fixed rate coupon bond, companies can cap their 
					interest costs which is very important for long term project 
					funding, and by keeping a call option at the end of a 
					certain Tenor, the company can have the flexibility of 
					redeeming the bond in an interest rate falling scenario or 
					in case of unexpected cash inflows. <br>
					<br>
					For banks, this will be an additional window for 
					participating in the long term infrastructure development of 
					the country, over and above their single borrower limits. By 
					classifying these investments in long dated bonds as ‘Held 
					for Trading”, banks can mitigate the problem of maturity 
					gaps in their long term buckets. Moreover, for investors 
					like PFs, MFs, insurance companies and HNIs in the market, 
					these bonds can be an additional investment opportunity. A 
					well developed secondary market can provide ample liquidity 
					to all investors in these long dated bonds. Another strong 
					argument in favour of developing a strong bond market is its 
					ability to create yield curves and transparency. This 
					assumes added significance in the GCC context where 
					government borrowings are minimal.<br>
					<br>
					There is a strong case for developing the fixed income 
					securities market in Oman. There is enough demand especially 
					from the banks, PFs and MFs whose presence are essential for 
					developing a vibrant bond market. The supply side 
					constraints need to be removed for ensuring steady supply of 
					good papers in the market and thus developing an active debt 
					market in Oman. By doing so, it can provide an important 
					additional source of funding for the corporate sector. 
					Liquidity in the secondary market can be improved by the 
					banks undertaking market making (by giving two way quotes) 
					in bonds which will also be a good source of income for the 
					banks. Such a market making exercise will ensure adequate 
					supply and provide liquidity to the bonds and help in 
					popularising them among the investors. <br>
					<br>
					An increase in the investment limit of banks on fixed income 
					securities may be considered by the CBO. Local banks and 
					large corporate houses along with the support of CMA need to 
					take the active role in this endeavour. A developed bond 
					market assumes added significance in the context of Oman 
					going in for lot of large infrastructure developmental 
					projects (power, water, airports, roads etc) which requires 
					huge long term project funding.<br>
