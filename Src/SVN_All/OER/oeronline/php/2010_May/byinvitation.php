						<td valign="top" colspan="2">
						<p align="justify" style="margin-left: 10px; margin-right: 10px" dir="ltr"><b><font face="Verdana" size="2">
						This The lack of popularity of the bond for fund 
						raising, minimal government borrowing programmes and the 
						tendency to hold on to it till maturity have created a 
						demand- supply gap</font></b></td>
