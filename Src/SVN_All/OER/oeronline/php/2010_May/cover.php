					<font face="Verdana" size="4" color="#C00000"><b>Sparkling 
					efficiency<br>
					</b></font><font face="Verdana" size="1"><br>
					</font><font face="Verdana" size="2"><b>The year 2009 was 
					full of challenges but Shell Oman Marketing Company found 
					opportunities in this environment by focussing on cost 
					reduction, delivering its plans and fostering a performance 
					driven culture. All these helped the company to achieve good 
					results, writes Visvas Paul D Karra<br>
					</b></font><font face="Verdana" size="1"><br>
					<br>
					Shell Oman Marketing Company (SOMC) achieved unprecedented 
					financial results amidst an unsettled global economic 
					environment and recorded the highest-ever net profit in its 
					history after tax of RO13.02mn compared to the previous 
					year’s net profit after tax of RO12.5mn. Despite the 
					uncertainty surrounding major global economies, the 
					company’s proactive management, as well as sound operational 
					performance were the principal drivers of SOMC’s commendable 
					financial performance in 2009.<br>
					<br>
					As a result of higher net income, the earnings per share for 
					2009 moved upwards to 130 bzs, compared to 125 bzs in 2008. 
					The final dividend of RO12mn i.e. 120 bzs per share 
					represents an increase of 4.4 per cent over the previous 
					year’s total final dividend of RO11.5mn.<br>
					<br>
					<b>Effective initiatives<br>
					</b>Such outstanding results have stemmed from the company’s 
					focus on cost-cutting initiatives that included all areas of 
					its business, in areas such as distribution, lubricants 
					blending as well as administrative and operating expenses. 
					The credit position has been monitored with more stringent 
					policies in place to manage any credit exposure and this has 
					further improved SOMC’s cash flow positions while minimising 
					any credit issues. SOMC’s turnover comes mainly from its 
					retail business, which contributes 53 per cent, while the 
					rest is from commercial fuels (21 per cent); lubricants (14 
					per cent); and aviation (12 per cent).<br>
					<br>
					An upbeat Faisal Al Hashar, managing director, SOMC says 
					that the script for this success story was planned much 
					before the actual impact of the global economic crisis. “In 
					a team of both Senior and middle management, we began by 
					working on the Vision 55 goal in which the company 
					envisioned two different scenarios in the middle of 2008 
					itself. One is what if oil prices collapse and the other was 
					what if the oil prices went up. And where would the 
					organisation be in these two situations and how to manage 
					the situations,” says Al Hashar.<br>
					<br>
					Continuing further he says, “I believe as an organisation, 
					we have once again exceeded our targets and in a number of 
					areas too. First, the team had delivered on a massive cost 
					reduction that covered all the areas of our businesses. This 
					has greatly influenced our ability to compete in an ever 
					increasingly competitive market but also allowed us the 
					flexibility to manoeuvre our business in accordance to the 
					prevailing market.”<br>
					<br>
					<b>Anticipatory measures<br>
					</b>This has been more specific to SOMC’s lubricants 
					blending plant where the exports were around 80 per cent. 
					For example, in 2008 the plant achieved a production of 
					around 100 million litres of lubricants while in 2009, the 
					plant production was scaled down to near 77 million litres. 
					The cost base required to service a 100 million is far 
					greater than 77 million and at the same time it was easier 
					to build and invest to meet increased demand rather than 
					having to adjust costs in a market that is shrinking.<br>
					<br>
					The reputation for high quality lubricants has enabled the 
					company to achieve higher penetration in both the local and 
					export markets despite the volatile economic conditions. A 
					project to repackage lubricants to attract both existing and 
					new customers was completed by the end of Q3, 2009. In 
					addition, the inland lubricants team has managed to renew 
					key contracts as well as gain new ones that have greatly 
					contributed to business growth.<br>
					<br>
					The company also initiated cost-cutting measures, and as Al 
					Hashar mentions, it was fantastic how the people reacted and 
					began to contribute to these measures. First of all, while 
					other organisations cut down on salaries, SOMC did not cut 
					down on HR benefits as it believes that people are its most 
					important assets.<br>
					<br>
					<b>Sound credit management<br>
					</b>“Another important thing which I also mentioned in last 
					year’s top OER Top 20 Debate and Awards Presentation 
					function in 2009, at CMA, is that our company will be 
					focusing on the generation of cash from its balance sheet, 
					will focus on costs reduction and will further focus on 
					enhancing our services to our customers.. We tightened the 
					belt by reducing the credit days, and it was amazing to see 
					how much cash was there in our hands. Moving forward, we 
					want to maintain these measures because the efficiency was 
					so great like for example we realised how fast an invoice 
					can be prepared. So our credit management showed a major 
					improvement with significant reduction in sales 
					outstanding,” adds Al Hashar. <br>
					<br>
					On the retail business side, SOMC focussed on three areas – 
					network development, operational excellence and marketing – 
					which produced the desired results. The growth initiatives 
					were led by investments in new-to-industry (NTI) sites; 
					rebuilding of old sites, focussing on consistently 
					delivering excellence in site operations and sustained 
					marketing efforts.<br>
					<br>
					Four old sites were rebuilt while six NTI sites were 
					commissioned taking the network number to 139 till date. 
					Here again, the company wisely took advantage of the decline 
					in construction costs to build the sites and was able to 
					reduce costs. The retail marketshare remains the highest and 
					is fairly stable in the face of an ever-growing number of 
					new sites introduced in the market. The average throughput 
					per site (ATP) stands at 9.72 million litres in 2009 and the 
					company’s retail sales grew by one per cent year-on-year. 
					August 2009 saw the highest-ever Shell Oman monthly sale of 
					122 million litres.<br>
					<br>
					<b>Reliability pays<br>
					</b>Many business factors changed in the aviation market in 
					the year 2009, and in early 2010 SOMC lost Oman Air contract 
					to its competitor. “We offer value to our customers that is 
					supported by outstanding service “end-to-end” and we try to 
					avoid offers that are based on price undercutting” says Al 
					Hashar.<br>
					<br>
					Nevertheless, he adds the company has important customers 
					like Royal Air Force of Oman (RAFO); Ministry of Defence etc 
					who want a company like SOMC that delivers on its promise 
					with reliability and enhanced customer value proposition. 
					The company continues to supply its fuel and services to 
					RAFO four bases in Masirah, Thumrait, Musandam and Salalah. 
					Further, in 2009, SOMC also bagged a contract to supply fuel 
					to all airlines that fly from India into Muscat 
					International Airport till April 2012.<br>
					<br>
					<b>Drawing customers<br>
					</b>Al Hashar says that the company enhanced its customer 
					service like efficiencies in delivery, invoicing and filling 
					on time etc which helped Shell Oman to gain more customers. 
					That created a barrier for customers to move out and in fact 
					the company gained more customers. “If you look at our 
					toplines the reductions in sales were not that big, whereas 
					if you look at the bottom lines we had huge growth in terms 
					of NIAT (net income after taxes) because of all the 
					processes that were put in place to manage and serve the 
					customers better.”<br>
					<br>
					All the sites have also been modernised, involving 
					significant investments, right from the underground tanks, 
					to toilets and all the way to the canopy to make sure that 
					customers see Shell Oman as the best. At the time of filling 
					also, we enhanced the customer experience in simple ways 
					like how the customer is greeted, cleaning the windshield 
					etc.<br>
					<br>
					All of this helped in drawing a lot of motorists to Shell 
					sites. There was an acceptable volume of increase at the 
					sites and this way the retail business has contributed 
					significantly to the results of the company.<br>
					<br>
					<b>Downstream One<br>
					</b>Shell Oman is excited about a new project, Downstream 
					One, which has been supported by the Board and approved by 
					the company’s shareholders at the last AGM. The project will 
					be costing around RO6mn over a 2 year period. Downstream One 
					works through a process known as ESSA, which in other words 
					means eliminate, standardise, simplify and automate.<br>
					<br>
					The strategy is to differentiate itself from the competitors 
					right from the fuel the company offers, to a whole new 
					customer experience. This would put the company in a 
					different league altogether. Although this programme may 
					affect the company’s bottomline in the short term, it will 
					deliver more value to shareholders in the long run. <br>
