                    </font><b><font face="Verdana" size="4" color="#C00000">
					THE OER TOP TWENTY<br>
					Year 2009<br>
					<br>
					</font><font face="Verdana" size="2">OER presents its annual 
					presentation of Oman’s Top 20 leading 
					listed companies for the year 2009</font></b><p align="justify" style="margin:3px 10px; " dir="ltr"> 
                    <font size="1" face="Verdana, Arial, Helvetica, sans-serif">
					<b><br>
                    The year 2009 will be remembered as a challenging year for 
					the world economies due to the recession caused by the 
					global financial crisis which started half way through 2008. 
					During 2009, an increasing number of countries registered 
					positive quarterly growth of gross domestic product (GDP), 
					along with a notable recovery in international trade and 
					global industrial production. World equity markets have also 
					rebounded and risk premiums on borrowing have fallen. Oman’s 
					consistent policy of economic diversification, with a 
					developing economy together with continued government 
					spending for major projects, assisted Oman in riding through 
					this storm quite successfully. <br>
					<br>
                    </b>The price of oil declined substantially during the year 
					2009. Oman crude realised an average price of $56.7 per 
					barrel in 2009 compared with an average price of $101 for 
					2008 and the budgeted price of $45 per barrel for 2009. Due 
					to higher oil prices than what was budgeted it is expected 
					that most of the estimated fiscal deficit of RO810mn would 
					be covered.<b><br>
                    </b><br>
					<b>Market performance</b><br>
					For the year 2009, the MSM index has advanced by 17.05 per 
					cent. This is compared to a negative performance of (39.12 
					per cent) in the previous fiscal year. The industry sector 
					was the outperformer of the year as the sector advanced by 
					72.31 per cent for the year 2009. Next was the banking and 
					investment index which appreciated by 41.59 per cent for the 
					year. The service and insurance index closed the year with a 
					total gain of 6.89 per cent. A total of 6.09 bn shares got 
					traded during the year amounting to an aggregate turnover of 
					RO2.28bn. The MSM30 index hit a low of 4,187.81 points 
					during the year while the high of the year was 6,785.92 
					points. With a return of 17.05 per cent for the year, the 
					MSM has been the second best performing market in the GCC 
					region after Saudi Arabia which advanced by 27.46 per cent 
					for the year. UAE (DFM) ended the year up by 10.22 per cent 
					and Qatar ended up by 1.06 per cent. Kuwait and Bahrain 
					ended the year down by 9.99 per cent and 19.17 per cent 
					respectively.<br>
					<br>
					The substantial decline in oil prices during the year was 
					overcome partly by increased oil production and increased 
					non-oil exports which resulted in a GDP growth rate of 3.7 
					per cent in 2009. Oil production increased during the year 
					by about of 7.7 per cent to 810,000 barrels per day. The 
					government’s continued emphasis on diversifying the economy 
					away from dependence on oil gained further momentum during 
					2009. Non oil exports grew by 2.9 per cent. The government 
					launched several major projects including extension of the 
					Seeb and Salalah airports, roads and highways.<br>
					<br>
					Performance of world financial markets after a scary 2008 
					revived thanks in part to aggressive central bank policies. 
					Emerging market equities, which led 2008 equity markets down 
					(-45.7 per cent), also led global equities back up in 2009 
					(62.8 per cent) as risk came back into vogue and US 
					investors deployed funds abroad. The MSCI AC World index was 
					up 58.7 per cent in 2009. Global equity markets finished 
					2009 up 62.8 per cent. Emerging Markets were the best 
					performing region (index) followed by Asia Pacific at 58.7 
					per cent, US 26.5 per cent, Europe 25.6 per cent and Japan 
					17.3 per cent.<br>
					<br>
					During the year 2009, the revenues of Oman’s 20 largest 
					companies showed a modest decline. Total revenues of the OER 
					Top 20 went down by two per cent to RO3,126mn. Corporate 
					performance of the year 2009, overall, was also down 
					primarily due to the global recession. The profits for the 
					year 2009 decreased by 12 per cent to RO425mn from the 
					RO480mn last year. The total market cap of the OER Top 20 on 
					December 31, 2009 was RO4,514mn, which was up by 11 per cent 
					compared to 2008. On March 31, 2010, the market cap of the 
					top twenty has once again gone up to RO4,684mn. The OER Top 
					20 companies represent 72 per cent of the total market cap 
					of the MSM of RO6,303mn at the end of 2009. The average P/E 
					ratio of the OER Top 20 based on the profits of the year 
					2009 and the share price on March 31, 2010 is 11.03 times 
					earnings.</font><p align="right" style="margin: 5"> <font face="Verdana" size="1"> 

