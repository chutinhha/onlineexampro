					his report to the shareholders for the year ended December 
					31, 2009 has stated that the increase in profit of 93.2 per 
					cent is mainly due to improvement in the average selling 
					price, lower procurement cost of imported clinker, 
					discontinuation of import of cement and increased investment 
					income. He points out that the company continues to be a ISO 
					9001-2000 and ISO 14001-2004 certified company. It has also 
					API-SPEC 10A quality monogram certification for 
					manufacturing and sale of oil well cement. The Board has 
					recommended a cash dividend of 37 per cent. <br>
