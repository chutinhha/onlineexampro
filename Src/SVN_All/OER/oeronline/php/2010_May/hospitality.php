				<p align="justify" style="margin: 3px 10px" dir="ltr">
				<font face="Verdana" size="1"><br>
				</font><b><font face="Verdana" size="4" color="#C00000">Salalah 
				gets its due share</font><font face="Verdana" size="2"><br>
				<br>
				Osama Mariam, CEO, Dhofar Tourism Company talks about his 
				expectations from the recently opened world-class five-star 
				Salalah Marriot Resort in a conversation with Akshay Bhatnagar<br>
&nbsp;</font></b><p align="center" style="margin: 3px 10px" dir="ltr">
				&nbsp;<p align="justify" style="margin: 3px 10px" dir="ltr">
				<font face="Verdana" size="1"><b>What 
				do you think about the pressure on the tourism sector in Oman as 
				a direct impact of the global economic downturn?<br>
				</b>It is correct that tourism has been affected however Oman 
				has not been affected much compared to the other GCC countries…<br>
				<br>
				</font>
				<b>
					<font face="Verdana, Arial, Helvetica, sans-serif" size="2" color="#CC3300">
				<img border="2" src="images/may-hospality.jpg" hspace="9" style="border: 2px solid #808080" vspace="12" align="right"></font></b><font face="Verdana" size="1"><b>But the occupancy ratio and room rates have gone down 
				considerably in Oman?<br>
				</b>Dhofar region is different compared to Muscat. Dhofar has a 
				different business mix than Muscat. In Salalah, there are just 
				two international hotels and ours is the third one. The market 
				has appetite for three international hotels. We receive tourists 
				from Europe in the period October to May. Whereas in July to 
				August we have the Khareef season which is a big attraction for 
				the residents from Oman as well as GCC countries. Due to this 
				business mix, the tourism sector in Dhofar region has not been 
				impacted as we have seen in Muscat and other tourism 
				destinations.<br>
				<br>
				<b>What is your vision for the newly opened Salalah Marriot 
				Ressort?<br>
				</b>Our objective is to position the property as an upmarket 
				resort. It has a number of unique selling propositions. Salalah 
				Marriot Resort is set in a premium location. It offers a wide 
				variety of restaurants and lounges. It provides a good range of 
				accommodations with different configurations to meet the various 
				requirements of guests from different markets. Then we have the 
				best location for diving. Mirbat is known for diving. This will 
				appeal majorly to our guests from the European market (Germany, 
				Switzerland, France, Italy etc.). In addition, we are the only 
				spa centre (from Bali) available in the Dhofar region. We are an 
				ideal destination for honeymooners as well.<br>
				<br>
				<b>What are your expectations on the business front?<br>
				</b>In terms of business, we are targeting leisure as well as 
				the MICE segments. In the first year, we are expecting about 50 
				per cent occupancy with an average room rate of RO45.<br>
				<br>
				<b>How much investment has been made in the making of this 
				resort and when do you plan to breakeven?<br>
				</b>It is RO20mn excluding the land cost. We hope to breakeven 
				in 10 years.<br>
				<br>
				<b>What makes you think that Salalah Marriot will be successful 
				as a MICE destination?<br>
				</b>Most of the regional corporate giants have hubs in Dubai and 
				Saudi Arabia. We offer a good destination to them for their 
				business related events. We offer a broad range of MICE 
				facilities. Our largest banquet hall can accommodate upto 350 
				persons. We also have a large number of rooms to accommodate 
				very large groups. The guests can do business and relax in the 
				resort. We are close to the city so people can visit it to enjoy 
				other attractions as well. Our location, price and the combined 
				package makes us quite attractive in the MICE segment.<br>
				<br>
				<b>How do you plan to offer the high standards of customer 
				service for which Marriot is globally renowned?<br>
				</b>This is the first Marriot property in the sultanate. This is 
				an ideal opportunity for Marriot to spread awareness about their 
				hospitality and brand values in the market. A significant number 
				of 160-strong staff at Salalah Marriot has the experience of 
				working in Marriot properties in other markets. Marriot has 
				provided adequate training to local staff in maintaining 
				international quality standards. In terms of infrastructure, we 
				have a good variety of offerings to cater to the needs of our 
				guests. For example, we have chalets for guests especially from 
				the GCC market who prefers privacy.<br>
				<br>
				<b>What needs to be done to grow the tourism sector in the 
				Dhofar region?<br>
				</b>Dhofar region has improved a lot on the infrastructure 
				front. However, we need to have an international airport in 
				place to provide direct flights for people coming from the GCC 
				region or Europe and other international destinations. It is 
				always more convenient to fly in directly rather than coming via 
				Dubai or Muscat. With an international airport, it will be easy 
				to promote the destination and grow tourism in the region. We 
				are expecting the first phase of the international airport to be 
				over by 2013 as per the plan.<br>
				<br>
				<b>What about the initiatives on the marketing side?<br>
				</b>I must say that the Ministry of Tourism for the last 3-4 
				years have really worked very hard to ensure its presence at the 
				major exhibitions and trade shows to promote the destination. 
				Unfortunately, many hotels have not been supportive enough and 
				did not participate in such promotions. We have to work very 
				hard with the Ministry of Tourism to support their initiatives. 
				We have to be present in all the major exhibitions to create 
				awareness and sell our product.<br>
				<br>
				<b>What is your game plan to promote the property in the Middle 
				East and international market?<br>
				</b>We will be promoting different packages to different tourist 
				segments and markets as per their unique requirements. With the 
				Internet, the world has become smaller. Through Marriot’s 
				website, database, global network, regional offices and sales 
				teams, our property will get the required push. We will also do 
				joint promotions to create visibility. Few FAM groups have 
				already visited us. We are trying to bring in large tour 
				operators from European countries such as Germany, France etc. 
				where Oman Air has direct flights to Oman. The idea is to create 
				awareness so that they are better positioned to sell the 
				destination. For this, we are tying up with Oman Air so that 
				they could sponsor the air travel and we sponsor the 
				accommodation.<br>
				<br>
				<b>What is the status of rest of the parts of Mirbat project?<br>
				</b>According to the master plan, we have finished the first 
				phase with the launch of Salalah Marriot. We were supposed to 
				open it in December last year but we opened in February this 
				year. The other phases will be taken up gradually. This year we 
				will start the detailed design and specifications for the second 
				phase, the largest phase in the development of the plan. It will 
				include the second hotel, furnished apartments, golf course, 
				water park etc. We will start with some of the infrastructure 
				work say roads, water, sewage and the marina. This second phase 
				development will be done over the next five years.<br>
				<br>
				<b>Have you made any changes in your original master plan with 
				the changing global market dynamics?<br>
				</b>So far we have not made any change up to the second phase. 
				We may reduce the scale of the water park.
