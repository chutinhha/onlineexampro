		        <td valign="top" width="100%"> 
				<p align="center" style="margin: 3px 10px"> 
					</p>
				<p align="justify" style="margin: 3px 10px"> 
					<font face="Verdana" size="2"><b>The Sultanate of Oman, 
					under the auspices of the Capital Market Authority has 
					demonstrated continued support for best practices in 
					corporate governance</b><br>
					</font></p>
                </td>
		</tr>
	<tr>
		        <td valign="top" width="100%"> 
					<p align="justify" style="margin: 3px 10px">
				<b>
					<font face="Verdana, Arial, Helvetica, sans-serif" size="2" color="#CC3300">
				<img border="2" src="images/may-legal.jpg" hspace="9" style="border: 2px solid #808080" align="right"></font></b><font face="Verdana" size="1">In 
					2002, the Sultanate implemented a model corporate governance 
					system applicable to joint stock companies listed on the 
					Muscat Securities Market (MSM) by endorsing the ‘Code of 
					Corporate Governance for MSM Listed Companies’ (the Code). 
					Endorsement of the Code went hand in hand with a series of 
					amendments to the Commercial Companies Law to tighten, 
					amongst other things, the requirements for the board of 
					directors and the management of a company, while at the same 
					time safeguarding the interests of shareholders and ensuring 
					minority shareholder protection and full and frank 
					disclosure. <br>
					<br>
					In putting together the Code, the CMA drew from global best 
					practices which it tailored to satisfy local market 
					requirements. The Code is viewed as a pioneer in its area 
					and represents regional best practice. To emphasise its 
					commitment to best practices of corporate governance, the 
					Code requires that the annual report of eac</font><font face="Verdana" size="1">h 
					MSM listed company contains a separate chapter on corporate 
					governance which highlights non-compliance with any 
					requirement of the Code. This is generally known as the 
					“comply or explain” rule. The company’s auditors are also 
					required to certify that the corporate governance report 
					contained in the annual report is free from any material 
					misrepresentation. <br>
					<br>
					More recently the CMA issued an administrative decision 
					establishing a Corporate Governance Centre (CGC) in Oman. 
					The purpose of the CGC is to train and guide directors on 
					the best practices in corporate governance. <br>
					<br>
					<b>Non-executive directors and independent directors<br>
					</b>The Code requires that the board of directors be 
					comprised of a majority of non-executive directors and that 
					at least one third of the directors (with a minimum of two) 
					are independent directors.<br>
					<br>
					To be a non-executive director a director shouldn’t draw a 
					fixed salary from the company in his capacity as director. 
					The CMA has clarified that a non-executive director must not 
					be a regular employee of the company and therefore should 
					not oversee the routine functions of the company on a 
					day-to-day basis. Non-executive directors therefore 
					introduce a separation between the day-to-day management of 
					the company and the policy-setting role which is entrusted 
					to the board.<br>
&nbsp;<br>
					An independent director in turn is a non-executive director 
					which should not, be it directly or indirectly (through his 
					next in kin), have occupied any senior position (such as 
					CEO, GM or similar) in the company for the preceding two 
					years. In addition, there should not be any relation between 
					an independent director and the company, its parent company 
					or any of its affiliates which may result in financial 
					transactions. Thus, not only should an independent director 
					be free from any influence of management (as is the case for 
					non-executive directors), he should neither have entered 
					into any material pecuniary relationship or transaction with 
					the company. It should be noted that the rules issued by the 
					CMA set further conditions to the nomination of members to 
					the board of directors (such as a minimum age, the absence 
					of any conflicting board position with another listed 
					company, acknowledgment that if one is a shareholder then he 
					shall continue to be a shareholder of the company during the 
					term of his mandate, etc.).<br>
					<br>
					The general meeting determines the annual remuneration and 
					sitting fees of the members of the board of directors within 
					the limits set by the Commercial Companies Code (such limits 
					are set as a percentage of the net profits which are in no 
					event to exceed the cap of RO200,000 with a sub-cap of 
					RO10,000 for sitting fees per director per annum). Any fees 
					paid or other benefits given to the directors must be 
					disclosed in the report of the board of directors to the 
					annual general meeting of shareholders. <br>
					<br>
					<b>Audit Committee<br>
					</b>The board of directors must constitute an audit 
					committee from amongst its members. The majority of the 
					audit committee must be composed of independent directors 
					and the chairman ought to be at all times an independent 
					director. <br>
					<br>
					The role of the audit committee is one of ensuring 
					compliance with rules and regulations applicable to the 
					company. The audit committee also plays a critical role in 
					mandating the external auditors as advisors to the company, 
					and in this capacity it has to ensure that the external 
					auditor’s independence is at no times compromised. <br>
					<br>
					<b>Auditor<br>
					</b>The general meeting appoints the company’s external 
					auditor from among the persons licensed to practice the 
					accountancy and auditing profession. The CMA has a veto in 
					respect of the appointment of the external auditor. It is 
					mandatory that the auditor is at all times independent. In 
					this regard the Code provides for a rotation system: where 
					one firm of auditors has served as auditor of the company 
					for four consecutive years, such firm cannot be appointed 
					for a fifth term unless a two-year cool-off period has 
					elapsed. Although absolute independence from the company 
					must at all times be guaranteed, the company’s external 
					auditor is allowed to perform certain advisory services, but 
					stringent conditions apply. In Circular No. E/12/2009 of 
					December 28, 2009, the CMA clarified its position and issued 
					a list of non-audit services that can be conducted by the 
					auditor subject to putting in place adequate safeguards 
					(such as Chinese walls) and other measures necessary or 
					appropriate so as to avoid any conflict of interest that 
					arises or may arise between the management of the company 
					and its audit function, and to ensure that the auditor’s 
					independence is not compromised. As indicated above, the 
					audit committee plays a vital role in this respect and it 
					has to approve any performance of any non-audit services by 
					the external auditor. <br>
					<br>
					<b>Conclusion<br>
					</b>Corporate governance continues to be a hot topic in the 
					Sultanate of Oman as it directly affects the performance of 
					any MSM listed company and therefore contributes to the 
					stability and success of the capital markets. The CMA’s 
					continued efforts to improve best practices and to educate 
					directors of MSM listed companies about corporate governance 
					are impressive and underpin investors’ confidence in the 
					Omani capital market. </font>
