                    </font><b><font face="Verdana" size="4" color="#C00000">
				Contagion, risk and currencies</font></b><p align="justify" style="margin: 3px 10px" dir="ltr">
				<font face="Verdana" size="1">
				<br>
                </font><b><font face="Verdana" size="2">There are so many 
				unanswered questions about how the financial markets are showing 
				an upswing despite negative economic indicators emerging from 
				many global quarters</font><font face="Verdana" size="1"><br>
                <br>
                </font></b><font face="Verdana" size="1">Greece has taught us 
				that contagion risk can never be underestimated in global 
				currencies, for those who failed to learn the lessons of the 
				sterling ERM carnage (1992), the Mexican peso devaluation 
				(1994), the Russian rouble default (1998) and the Asian currency 
				meltdown (1997- 1998), the sovereign default in Argentina (2000) 
				and currency collapses in Turkey (2001). I cannot forget these 
				crises because I lived and traded them all, even as I trade the 
				Greek tragedy in real time now.<br>
				<br>
				Chancellor Merkel’s grudging EU-IMF financial lifeline does not 
				inspire confidence. Greek, Italian, Portuguese and even German 
				bond auctions have been poorly received by investors. Even in 
				the US, with $2trn in Uncle Sam paper (US Treasury bills, notes 
				and bonds, thanks to Obama’s epic budget deficits), needed to be 
				financed in 2010, government bond markets are an accident 
				waiting to happen. There are profound disagreements in the 
				Eurozone about Greece. This is not bullish for the Euro, 
				particularly since US private payroll growth has finally resumed 
				after three years. In short, I believe 1.25-1.28 Euro is not at 
				all unrealistic by the end of 2010.<br>
				<br>
				The Conservative lead has widened even as UK economic data has 
				improved. This is the reason for the impressive sterling rally 
				from 1.48 to 1.55 in the first half of April. Sterling- Euro is 
				clearly the natural winner if David Cameron moves into Downing 
				Street.<br>
				<br>
				<b>An FX disaster<br>
				</b>There are also FX risks brewing in the commodities markets. 
				I am troubled that oil prices are $85 despite the fact that US 
				gasoline and crude inventories are at record levels and OPEC 
				compliance is 53 per cent, down from 86 percent last March 2009. 
				How long can Saudi Arabia, UAE and Kuwait tolerate open quota 
				cheating from other OPEC members? Why is the Baltics Dry Freight 
				Index falling if the world economy has so convincingly recovered 
				from the worst recession since the 1930’s? <br>
				<br>
				What happens to crude oil if Saudi Arabia boosts production to 
				defend market share in a glutted market? What happens to $8000 
				copper if Chinese hoarding demand evaporates, the Fed tightens, 
				copper warehouse inventories in London and Shanghai are bloated? 
				Does this not mean that emerging markets commodities curre- 
				ncies are a clear short against the dollar? Absolutely. Who? The 
				South African Rand (ZAR) is overvalued at 7.25 against the 
				dollar. So is the Brazilian Real at 1.76 against the dollar. A 
				global commodities crash is coming. It is only a matter of time. 
				Fasten your seatbelts.<br>
				<br>
				Equities can also shift the calculus of risk in global foreign 
				exchange. I am troubled by the excessive bullishness in world’s 
				financial markets. Markets bottomed last March when the world 
				indices priced financial Armageddon. Markets could well top when 
				the world prices complacency, as the Chicago Volatility Index 
				(VIX) at 15 and pre-Lehman credit risk spreads demonstrated. If 
				Lord Keynes taught me to buy fear and sell greed, it surely 
				makes sense to take risk off the table with the American index 
				at 11,000 Dow, 2,450 NASDAQ and 1,200 on the S&amp;P.<br>
				<br>
				<b>Bubble in the making<br>
				</b>With almost all index shares above their 50-day moving 
				averages, I am convinced Wall Street is overbought in the short 
				run. The world’s emerging markets show clear signs of a 
				valuation bubble. Does Sensex 18,000 really deserve to trade at 
				above three times book value and 20 times earnings at a time 
				when the Reserve Bank of India (RBI) will tighten monetary 
				policy to combat inflation, when the repo rate will rise another 
				300 points? Does it make sense to own a property in Brazil when 
				China could be on the eve of history’s next global property 
				bubble and financial crash thanks to the hot money tsunami 
				attracted to the Middle Kingdom by the yuan revaluation.<br>
				<br>
				After all, did this not happen to Japan in the 1980s after the 
				revaluation of the yen at the Plaza Accords (US diktat actually) 
				attracted the world’s hot money to Japan? Economic textbooks 
				tell us that a currency revaluation dampens imported inflation. 
				Wrong. Not in China, as it imports oil, copper, nickel, iron 
				ore, electronic components and software. The low trading volumes 
				on the NYSE is a testament to the risk of a major summer bear 
				panic attack on Wall Street. This could cause risk aversions to 
				spike. The result? A yen rally. A higher dollar against emerging 
				markets. A $300 drop in gold and $50 crude oil. <br>
				<br>
				<b>Understanding risk aversion<br>
				</b>It amazes me that so few market oracles in the Gulf 
				understand that global risk aversion is the kiss of death for 
				highly indebted, MENA/ GCC markets. A VIX at 15 was last 
				witnessed in May 2008 and October 2007, two bull market and 
				panic peaks. At 12-13 VIX, the risk of a global stock market 
				crash will become too compelling to ignore. The biggest asset 
				bubble in the world is now in Chinese properties and commodities 
				like copper. A Fed tightening or a hard Chinese landing (rate 
				hikes, inflation, NPL, yuan reval, property crash, banking 
				crisis) could well mean the world bull market is living on 
				borrowed time. If I am right, risk aversion will mean the 
				Australian dollar can drop 700 points from 0.93 to 0.85 this 
				summer. This is a potential profit prospect of $700,000 on a 
				$10mn forward FX position. The risk? Lord Keynes also warned us 
				that financial markets could stay irrational a lot longer than 
				we can stay solvent. As they say on Wall Street, life is lived 
				forwards, but only understood backwards! So is global macro 
				trading themes!<br>
				<br>
				The SEC lawsuit against Goldman Sachs could have ominous 
				implications for risk. It is no coincidence that the VIX shot 
				up, stock exchanges fell worldwide and gold plunged $20. A spike 
				in risk and a fall in commodities meant the kiss of death for 
				the Australian dollar and the Brazilian real. Lehman’s failure 
				was the black swan event of 2008. Could Goldman Sachs’s shame be 
				the black swan event of 2010? 
