            Facts, Figures &amp; Feats</font></font><p>
            <b><font face="Verdana" size="1">Buoyant oil prices throughout 2002 
            kept Oman's economy on track and improved investor sentiments on the 
            local bourse. The following analysis and ranking by Mukhtar Hasan, 
            based on last year's financial results of Oman's largest listed 
            companies, allows you to check on the progress of the top 20 
            blue-chips. The OER 20 has been verified by KPMG. Read on</font></b></p>
            <p><font face="Verdana" size="1">The year 2002 has been an 
            encouraging year for Oman's economy in general and the Muscat 
            Securities Market (MSM) in particular. An increase in the oil prices 
            during 2002, with the average price increasing by six per cent to 
            US$24.29 per barrel, resulted in an improvement in the planned 
            budget deficit of the Sultanate. Cash liquidity during the year 
            eased slightly and liquidity of stocks improved substantially. 
            Trading volumes amounted to RO231 million in 2002 compared to RO164 
            million in 2001, an increase of 41 per cent. A rise of 26.2 per cent 
            was experienced at the MSM, as the Index started 2002 at 152.08 
            points and closed the year at 191.86 points. Also, the MSM was the 
            second best performing Arab stock market in 2002 after the Doha 
            Stock Exchange (DSE) in Qatar.<br>
            During the course of the year, certain amendments were made to the 
            Commercial Companies Law in addition to the Capital Markets 
            Authority (CMA) publishing guidelines on corporate governance. These 
            landmark developments have highlighted the responsibilities of the 
            Board of Directors in ensuring transparency and maximising 
            shareholder value. <br>
            On the economic development front, the government continued its 
            emphasis of diversifying the economy away from dependence on oil. 
            The long awaited Oman India Fertiliser project achieved financial 
            closure, and several power projects went into the construction phase 
            including three major ones at Barka, Al Kamil and Salalah. Decisions 
            were made for going ahead with the third LNG train, a refinery in 
            Sohar and a polypropylene plant. And Zubair Group announced a major 
            tourism project complex to be located at Bander Jissah. <br>
            On the international scene, 2002 saw the most stock markets in the 
            world closing the year at a considerable low for the third 
            consecutive year. Many scandals and corporate crimes were exposed in 
            2002 including Enron, Worldcom, Adelphia and ImClone, which did not 
            help revive the confidence levels of the investors. Accounting 
            practices followed by international audit companies were questioned, 
            and one of the largest audit firms - Arthur Andersen - closed down. 
            The US Securities and Exchange Commission embarked upon corporate 
            governance reforms to ensure avoidance of such practices in the 
            future and to rebuild investors' confidence. <br>
            <br>
            <b>The OER 20 <br>
            </b>During 2002, the revenues of Oman's 20 largest companies 
            continued to grow. Total revenues for the OER 20 companies went up 
            by 5.3 per cent to RO879 million. After the dismal corporate 
            performance in 2001, the overall profits for the year 2002 increased 
            dramatically by 180 per cent to RO88 million. The total market 
            capitalisation of the OER 20 companies on December 31, 2002 was 
            RO845 million, which was also up by 22 per cent compared to 2001. 
            The OER 20 companies represented 42 per cent of the total market 
            capitalisation on the MSM at RO2 billion at the end of 2002. The 
            average price to earnings (P/E) ratio of these companies on April 
            20, 2003 was 10:9. <br>
            <br>
            <b>Who's in, who's out? <br>
            </b>Well, not too many surprises. There are no new entrants in the 
            OER 20 and the top four have held their ground. Overall, the ranking 
            of Oman's 20 largest companies in order of revenue gives us a list 
            that is dominated by banks and petroleum distribution companies.<br>
            Shell Oman Marketing Company continues to remain by far the largest 
            public company in Oman. The company showed a growth of 10.59 per 
            cent during the year 2002. Nicholas Pattison, retiring chairman and 
            managing director of the company, in his report to the shareholders 
            explains that a major influence on the revenue growth has been the 
            strong demand for aviation fuel from the Royal Air Force of Oman. 
            This demand was met through the provision of excellent logistical 
            support and by building a new mini-depot in the South. Pattison goes 
            on to say that the market suffered from poor payment and a number of 
            bad debts. Shell has taken a much tougher line on non-payments or 
            late payments and that is already beginning to positively affect the 
            performance. The company has declared a dividend of 600 baisa per 
            share, which equates to a dividend yield of 8.96 per cent. Three of 
            the top five most profitable companies in Oman are banks. The growth 
            rates during the year sound phenomenally high in the case of banks 
            because of a bad 2001, where they had to make provisions for major 
            corporate loans. Most of the banks seem to have bounced back from 
            this trend set last year. <br>
            BankMuscat was once again the most profitable bank and the second 
            largest company in terms of turnover in Oman in 2002. The bank has 
            achieved this position by attaining a growth in 2002 profit of about 
            196 per cent. Its chairman, Abdul Malik bin Abdullah Al Khalili, 
            states in his report to the shareholders that during 2002, the bank 
            successfully completed its merger with Industrial Bank of Oman, 
            acquired and commenced operations of the Bahrain branch and 
            integrated the operations obtained from Al Ahlia Securities. The 
            bank also successfully integrated the credit card merchant acquirer 
            business, which it acquired from HSBC Oman. Khalili goes on to say 
            that BankMuscat commands a market share of over 34 per cent in terms 
            of total assets, 37 per cent of total credit and over 34 per cent of 
            total customer deposits. The bank's board has proposed a cash 
            dividend of 15 per cent and a stock dividend of five per cent for 
            2002. <br>
            Oman International Bank (OIB) has shown the highest growth in profit 
            by a staggering 839 per cent. Unfortunately, the chairperson's 
            statement was not available at the time of going to press, but the 
            major reason for this growth has been, as explained earlier, a 
            substantial recovery of previous year's provisioning, which somewhat 
            distorts the actual performance for the year. <br>
            The second best performer for the year is Oman Holdings 
            International. Maqbool Hameed Al Saleh, chairman, in his report 
            stated that, in 2002, the government did, and still continues to 
            approve major investments in infrastructure projects. The company 
            has benefited from these investments and is reflected in the 
            performance for the year, whereby it has declared a dividend of 12 
            per cent. Saleh goes on to say that the company has invested in a 
            business venture in the UAE as the first step to globalise its IT 
            operations. The company continues to look at business opportunities 
            to expand and complement its areas of activities.<br>
            Three of the top five companies that have the highest amount of 
            equity employed in Oman are banks. <br>
            National Bank of Oman is the third highest capitalised company in 
            the country. Khalfan Bin Nasser Al Wohaibi, chairman, said in his 
            annual report that, in 2002, the bank successfully accomplished five 
            key business objectives as a direct consequence of which operating 
            income rose from RO44.7 million to RO52.4 million. However, Wohaibi 
            says that it is much less gratifying to report that once again 
            during 2002, as in 2001, the requirement for additional provisioning 
            with respect to impaired advances has more than surpassed the 
            generation of operating income. The bank made a total provision for 
            credit losses of RO35.3 million over and above the RO31.5 million 
            provided in 2001. This has resulted in a net loss of RO0.262 million 
            for 2002. Al Wohaibi goes on to say that the bank has already 
            instituted a comprehensive restructuring of all aspects of risk 
            management and control processes. The bank's management and Board of 
            Directors are committed to revive the bank's profitability. <br>
            Once again, four of the top five companies in Oman having the 
            largest market capitalisation are banks. <br>
            The chairman of Bank Dhofar Al Omani Al Fransi, Eng. Abdul Hafidh 
            Salim Rajab Al Aujaili, in his statement has said that, during 2002, 
            the bank has continued its excellent achievement as most financial 
            indicators recorded remarkable growth. Aujaili goes on to say that 
            the gross loans and advances grew by 4.4 per cent from RO272 million 
            to RO284 million. This reflects a strong performance compared to its 
            peers when loans and advances of the entire banking sector increased 
            by only one per cent in 2002. The Board has recommended 15 per cent 
            cash divided for the year. The shareholders approved the proposal 
            for the merger of Majan International Bank and the merger is now 
            effective. Aujaili further says that the Board has appointed a firm 
            of international consultants to review and assess various 
            organisational and managerial aspects of both banks and to submit 
            their recommendations based on best practices in the banking 
            industry. <br>
            Dhofar Insurance Company has shown the best return on equity 
            employed among all the OER 20 companies. <br>
            Salim Mubarak Al Shanfari, the Chairman of Dhofar Insurance Company, 
            said that the results attained by the company were outstanding in 
            every aspect. Total premium income for the year 2002 amounted to 
            RO37.29 million as compared to RO22.44 million in 2001, showing a 
            proportional increase of 66 per cent. Shanfari goes on to say that 
            the company today is in the forefront and can be considered as one 
            of the largest insurance companies in the Arab World. The Board has 
            recommended a 30 per cent cash dividend for the year. Shanfari 
            states that the decision taken by the shareholders to increase the 
            paid-up share capital by 25 per cent as free bonus shares is a major 
            step taken to increase the company's capacity for 2003, strengthen 
            financial standing and provide a strong base for financial security.<br>
            The top four earnings per share growth companies are identical to 
            the top four by growth of profit. <br>
            The chairman of Ominvest, Taya Bin Jandal Bin Ali has stated in his 
            report to the shareholders that, during 2002, economic conditions in 
            Oman improved due to firmer oil prices and government spending 
            focused on projects. However, the US and other developed markets 
            continued to present a difficult investing environment elsewhere. In 
            spite of this difficult background, the company's policy of 
            diversifying its asset base has resulted in substantially improved 
            results. The Ominvest chairman also says that their major 
            subsidiary, Oman Arab Bank, reported substantially improved results 
            on the back of higher net interest margins and gains recorded on 
            investment portfolio. The Board has recommended a 20 per cent cash 
            dividend for the year. He is hopeful that the strength of the 
            company's major investments in Oman and the diversification of the 
            portfolios will contribute to improved results in 2003 even though 
            the global economy remains troubled and may be impacted by recent 
            developments in the region.<br>
            Hindsight is always 20:20 and this is a wonderful thing. If 
            investors had purchased the shares of BankMuscat at the beginning of 
            2002, they would have more than doubled their wealth by the end of 
            the year. BankMuscat tops the list of companies registering the best 
            share price growth for the year. <br>
            Samir J. Fancy, chairman of Topaz Engineering &amp; Marine, in his 
            report to the shareholders stated that a robust performance of the 
            ship-owning division contributed significantly to the 2002 results.<br>
            During the last quarter of 2002, the company sold an older vessel at 
            a profit and added two new vessels to its fleet. Fancy states that 
            the ship repair business reported profits despite volatile market 
            conditions and after reporting losses during the previous two years.
            <br>
            The Board has recommended a seven per cent cash dividend for the 
            year. Fancy further says that the region remains in a state of 
            uncertainty as the geopolitical situation unfolds around us. Subject 
            to this variable, there is a clear expectation for further growth in 
            both revenues and profitability during 2003.<br>
            Dhofar Insurance Company ranks number one in terms of dividend yield 
            offered to its shareholders. <br>
            The chairman of United Power Company (UPC), Murtadha Ahmed Sultan, 
            in his annual report has stated that the power plant at Manah has 
            been running efficiently. UPC's Board of Directors have recommended 
            a cash dividend of RO2.942 million, which brings the total ordinary 
            dividend for the year to 17 per cent. <br>
            Recently, Sultan stated that the company3s share value at the MSM 
            had fallen. This may have been caused due to reliance on some 
            publications and analysis made by third parties. The company gave 
            clarifications about the situation to the CMA confirming that there 
            were no operational or technical issues connected to the company 
            and, therefore, the company was not aware of any reason for the fall 
            in price.</font></p>

