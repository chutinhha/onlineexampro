                    </font><b><font face="Verdana" size="4">Double whammy</font></b><font face="Verdana" size="1"><br>
                <b>The fall in global oil prices added to the woes of the 
                regional markets which are reeling under the impact of the 
                global credit crisis<br>
                </b><br>
                Muscat Securities Market (MSM) has been struggling to sustain 
                above the sentimental 6000 mark for the past few weeks, each 
                time slipping below the mark due to excessive selling pressure. 
                The benchmark MSM 30 index lost 4.1 per cent (or 257 points) to 
                close at 5966 led by the Banking and Industry sectors. Investors 
                appear nervous on building long positions at current levels 
                since global markets are very volatile and any negative news 
                will have a more intense impact on regional markets including 
                MSM. Persisting weakness in oil prices adds to the worries of an 
                already weak market.<br>
                <br>
                <b>Plummeting trade volumes<br>
                </b>Brokerage businesses have been adversely affected due to the 
                thinning of market turnover, which slipped to RO3.9mn (average 
                per day) from about RO10.8mn in the previous month. Optimism 
                generated over the introduction of new market stabilisation fund 
                by the Capital Market Authority also fizzled out after a while, 
                keeping the traders away.<br>
                <br>
                In corporate events, National Gas Company, announced the 
                formation of a new 50:50 Joint Venture “Unigaz” with paid up 
                capital of RO150,000, in partnership with Unigaz International 
                WLL, Bahrain. The primary activities of this company will be LPG 
                marketing and engineering of gas systems. In another important 
                development, the Board of Directors of OIFC at their meeting 
                held on December 2, 2008, resolved to reverse the earlier 
                decision taken on October 29, 2008 to buy back the shares of the 
                Company to the extent of 10 per cent of the capital of the 
                company.<br>
                <br>
                The Board of Directors of Al Jazeera Steel Products Company 
                informed the market that the company’s shares will be listed on 
                the Dubai Financial Market on December 18, 2008. The communiqué 
                also claimed that Jazeera Steel will be the first company in 
                Oman to have its shares listed in Dubai Financial Market.</font><p align="justify" style="margin: 3px 10px">&nbsp;<p align="center" style="margin: 3px 10px">
