                <b>Regional downturn<br>
                </b>The one month period ended December 15, 2008 was another 
                tough one for the GCC markets with the MSCI GCC index dropping 
                one per cent during this time amidst lacklustre trade. Most 
                markets remained closed during the second week of December owing 
                to Eid Al Adha holidays. All the major markets except for Qatar 
                and Kuwait registered sharp declines with the Dubai Financial 
                Market topping the list and ending down 7.5 per cent. Most major 
                indices ended in red with real estate and investment indices 
                leading the pack. DFM is the biggest YTD decliner in the GCC 
                having fallen 70.8 per cent since January 2007. The Bahrain 
                Market was the next poor performer for the month and the market 
                index lost 5.9 per cent. The Saudi TASI lost 4.7 per cent during 
                the month with moderate selling seen across all major sectoral 
                indices. Declining oil prices fuelled the negative sentiments in 
                the petrochemical sector, and the industrial, investment and 
                banking indices accentuated the decline. <br>
                <br>
                Qatar’s Doha Securities Market and Kuwait Stock Exchange were 
                the two markets which ended on a positive note with their 
                respective general indices gaining 10.4 per cent and 3.2 per 
                cent. While favourable news flow on share buybacks propped the 
                DSM up, Kuwaiti investors remained hopeful of an intervention by 
                sovereign funds to stabilise the market.</font><p align="justify" style="margin: 3px 10px">&nbsp;<p align="center" style="margin: 3px 10px">
