		        <td valign="top" width="100%"> <p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
                    </font><b><font face="Verdana" size="4">Larger than life 
                image</font></b><font face="Verdana" size="1"><br>
                <b>Arguably, Ben Hogan is the greatest ball-striker ever to play 
                the game. OER peeps into the life of the legendary golfer</b><br>
                <br>
                Ben Hogan was, by most accounts, the greatest golfer of his 
                time, and still stands as one of the greatest of all time. In 
                292 career PGA Tour events, Ben Hogan finished in the Top three 
                in 47.6 per cent of them and in the Top 10 in 241. Arguably, he 
                is the greatest ball-striker ever to play the game; even today, 
                when a player is making great contact with the ball, the term 
                “hitting it like Hogan,”
                is evoked.<br>
                <br>
                <img border="0" src="golf.jpg" align="left" hspace="8" vspace="6" width="193" height="457"><b>Major wins </b><br>
                His nine career professional Major Championships tie him with 
                Gary Player for fourth all-time highest championship victories, 
                trailing only Jack Nicklaus (18), Walter Hagen (13), and Tiger 
                Woods (10). Statistics of Hogan’s career reveal very little of 
                the legend. A total of 62 USPGA titles, four US Opens, two US 
                Masters, two USPGA Championships and one British Open, is only 
                half the story. <br>
                <br>
                Hogan had so much more to overcome than his quest for golfing 
                perfection. The early suicide of his father left a profound mark 
                on the young boy. His unsuitably small frame for golf and a 
                temperament that was completely unable to interact with the 
                fawning galleries – lest it interfere with the shot at hand. A 
                head-on collision with a greyhound bus and subsequent injuries 
                led to embarrassing putting yips and a confounding hook that 
                would rear its ugly head at crucial times in tournaments. For 
                years he felt plagued by the hook but that didn’t prevent him 
                from becoming virtually invincible in his best years.<br>
                <br>
                Hogan on the golf course exemplified grim determination and a 
                fiercely Irish-American competitive nature. With head down, eyes 
                focussed upon his shoe laces and invariably puffing on a long 
                cigarette, Hogan housed a low regard for idle chit-chat on the 
                course. Discussions with fellow professionals, if any, were 
                along the lines of, “Who’s away?” “What did you score?” He 
                simply hated to lose and worse, hated to hit a golf shot that 
                was even just a little off perfect. <br>
                <br>
                <b>Personal traits<br>
                </b>In essence, Ben Hogan really had two golf careers. The first 
                one was the underwhelming winless years; 1931-39. (save for one 
                fourball tournament with Vic Ghezzi). Invariably Hogan would 
                turn up at a tournament with little money to speak of and 
                experience real strain at the prospect of having to support his 
                wife, Valerie. During an especially lean period, he entered one 
                West Coast tourney with a bankroll of just 15 cents. At another 
                tournament, Hogan took the opportunity of jumping the boundary 
                fence of the golf course and skilfully stripped an orange tree 
                of its fruits, returned and played on without allowing his group 
                to fall too far behind.<br>
                <br>
                When people talk of Hogan today, you can be sure it is the 
                middle to latter career they refer to; the spectacularly 
                successful period from 1940 onwards. He took the golfing world 
                apart and was always the man to beat. He was leading money 
                winner in 1940, 1941, 1942, 1946 and 1948. </font><p align="right" dir="ltr" style="margin-left: 10px; margin-right: 10px">
