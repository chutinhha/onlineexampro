                    </font><b><font face="Verdana" size="4">An open economy <br>
                </font></b><font face="Verdana" size="1"><b>The book offers a 
                panoramic survey of India’s continuing reform challenges and is 
                a valuable resource for practitioners, policy makers, students 
                and scholars, writes Raj Shekhar Chandola<br>
                </b><br>
                <img border="0" src="bc.jpg" align="left" hspace="8" vspace="6" width="175" height="343">Jagdish Bhagwati, a distinguished economist and a professor at 
                Columbia University, was a strong critic of the state-dominated 
                economic regime in India during the planning era and in the 
                1970s and 1980s he was one of the few asking for the opening up 
                of the Indian economy to market forces. Hence when India went in 
                for reforms that favoured the market in the early 1990s, he felt 
                triumphant. The book reflects this mood.<br>
                <br>
                <b>A bird’s eye view<br>
                </b>This book is a compilation of the papers presented at the 
                India: An Emerging Giant conference held in October 2006 at the 
                Columbia University, USA. It also includes transcriptions of a 
                panel discussion and comments by participants. Its contributors 
                include world’s leading economic experts, long time participants 
                in India’s policy debates and current and past political 
                leaders.<br>
                <br>
                The first essay is by Arvind Panagariya who makes a critical 
                analysis of the pattern of growth since the reforms and contends 
                that while in the short period there has been a significant fall 
                in the share of agricultural output in GDP, the share of 
                industry, particularly manufacturing, has not grown. T.N. 
                Srinivasan analyses the unfinished reform agendum in the fiscal 
                sector and asks for changes in two major areas. He argues that 
                the Finance Commission be replaced by a permanent Fiscal Review 
                Council to provide a forum for the Centre and the states to 
                discuss each other’s fiscal policies in a common forum and wants 
                the Planning Commission to be converted into a Fund for Public 
                Investment. <br>
                <br>
                <b>Infrastructure woes <br>
                </b>Frank A Wolak deals with the Indian electricity supply 
                industry and argues that the major challenge for restructuring 
                the industry is to establish a regulatory process that protects 
                the interest of consumers while allowing suppliers to earn a 
                reasonable return on investment. Ashish Arora records the 
                phenomenal growth of the Indian software industry and its 
                contribution to India’s rapid increase in exports but doubts 
                whether India’s university system can cope with the quantity and 
                quality of personnel required to sustain the growth of the 
                industry. He asks for the higher educational sector to be opened 
                up to foreign institutions and capital.<br>
                <br>
                Today, it seems that growth should not be the only objective of 
                economic policy because when markets bring the system to a near 
                collapse, it can be saved only by the state and not by capital 
                and its owners. </font></p></td>
