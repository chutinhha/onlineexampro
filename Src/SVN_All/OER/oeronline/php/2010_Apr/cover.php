		        <td> 
				<p align="justify" style="margin: 3px 10px" dir="ltr">
				<font color="#3778FF"><b><font face="Verdana" size="1">The</font></b></font><font face="Verdana" size="1"><font color="#3778FF"><b> 
				banking industry worldwide got severely impacted by the global 
				financial crisis? How has Oman’s banking sector faced this 
				challenge?<br>
				</b></font><font color="#333333">The international financial 
				crisis had been a global concern, and countries with strong 
				fundamentals and sound financial systems have also been affected 
				by the concerns and uncertainties being transmitted from the 
				markets of the developed countries. Oman’s banking system had no 
				direct exposure to any of the toxic financial products and any 
				of the distressed international institutions, and in that sense, 
				they remained insulated from the direct adverse ramifications of 
				the global financial crisis. <br>
				<br>
				The Central Bank of Oman (CBO), nevertheless, recognised that 
				the global financial crisis, and its manifestation in the form 
				of a severe global credit squeeze, may have some indirect 
				effects on the liquidity and credit conditions in Oman’s banking 
				system for a brief period at the beginning of the crisis, which 
				the CBO had responded proactively with appropriate liquidity 
				enhancing policy measures. The CBO amended the reserve 
				requirement regulation reducing the reserve ratio from eight per 
				cent to five per cent of deposit liabilities effective December 
				2008, thus ensuring more structural liquidity in the banking 
				system. The lending ratio regulation was eased to 87.5 per cent 
				from 85 percent to avoid any immediate credit contraction due to 
				any regulatory constraint. <br>
				<br>
				Further, concerned about any possible tightening of access to US 
				dollar liquidity in the international markets, and the 
				associated concerns about rollover of short-term US dollar 
				liabilities and financing of project loans already committed in 
				USD, the CBO in consultation with the government offered access 
				to USD liquidity of upto $2bn to the local banks. Usage of this 
				facility has remained very modest and currently outstanding 
				figures reveal borrowings of around $100mn only. These measures 
				along with a strong regulatory framework was able to provide 
				needed stability and confidence to the Sultanate’s financial 
				sector, particularly at the height of the global financial 
				uncertainty.</font><font color="#808080"> <br>
				</font><br>
				<font color="#3778FF"><b>CBO has been a proponent of a strong 
				and effective regulatory regime. Did this approach help banks to 
				stand up to the crisis better?<br>
				</b></font><font color="#333333">The main challenge for the 
				banking sector in Oman, like in many other countries is to 
				continuously remain vigilant with respect to potential systemic 
				weaknesses and address them in a timely and correct manner. From 
				the CBO’s perspective, addressing issues such as contagion could 
				be in the form of policy adjustments and strengthening of the 
				regulatory framework. On the banking supervision front, CBO has 
				been strong proponent of a strong and effective regulatory 
				regime with the objective of strengthening the banking sector 
				without compromising its efficiency and ability to innovate and 
				spur economic growth. As a matter of fact, Oman’s banking system 
				was relatively less affected by the recent international 
				financial crisis as we were not into the business of complex 
				derivative products. <br>
				</font><br>
				<font color="#3778FF"><b>CBO has been promoting the idea of a 
				financial supervisory framework that includes risk based 
				supervision. What does this idea stand for and in what ways will 
				this help banks?<br>
				</b></font><font color="#333333">The current supervisory 
				approach of CBO is largely driven by on-site examination and is 
				supplemented by off-site monitoring. The on-site examination 
				process is based on the CAMEL approach, where capital adequacy, 
				asset quality, management aspects, Earnings, and liquidity of 
				supervised entities (ie banks, FLCs and MECs) are examined 
				keeping in view the requirement of provisions of the Banking Law 
				of 2000 and Regulations issued thereunder. It is argued that the 
				current supervisory process, which is a ‘one-size-fits-all’ 
				approach, is not necessarily tailored to the risk profile of a 
				supervised entity. Therefore, in order to respond positively to 
				the developments in the domain of banking regulation and 
				supervision as also the global trends, CBO has decided to move 
				over to ‘Risk-based and Consolidated Supervisory’ (RBS) regime.<br>
				<br>
				The RBS approach is intended to result in an effective and 
				efficient process for monitoring and assessing, on an on-going 
				basis, the safety and soundness of supervised entities. The RBS 
				approach entails focusing and dedicating supervisory resources 
				to identify activities and practices of greater risk to the 
				soundness of supervised entities, and also to identify 
				higher-risk institutions. Once those activities and institutions 
				have been identified, supervisory attention is turned to 
				assessing and measuring those risks, including examining how the 
				risks are mitigated. This approach makes it possible to more 
				efficiently deploy supervisory resources, whether staff, 
				financial or technical, to address the identified risks. <br>
				<br>
				Under the RBS approach, it is necessary that risk profiles of 
				all supervised entities are prepared, maintained and updated on 
				a continuous basis. The intensity of supervision and the extent 
				and focus of supervisory action will depend on the perceived 
				risk profile of a supervised entity. <br>
				</font><br>
				<font color="#3778FF"><b>How has the banking sector performed in 
				terms of assets growth, profit levels, capital adequacy ratios 
				in 2009?<br>
				</b></font><font color="#333333">The banking sector in Oman 
				continued its growth during 2009 as it witnessed increase in all 
				major banking aggregates despite the deceleration reflecting the 
				cyclical downturn of the economy. Provisional figures of 
				commercial banks showed a modest growth in total assets of 2.9 
				per cent to 14.2bn rials in December 2009 compared to 13.8bn 
				rials in December 2008. <br>
				<br>
				Total outstanding credit rose by 6.2 percent to 9.8bn rials at 
				the end of December 2009 from 9.25bn rials at the end of 
				December 2008. Aggregate deposits also registered a growth of 
				six per cent from 8.6bn rials to 9.1bn rials. Provisional net 
				profits of commercial banks stood slightly lower at 190.8mn 
				rials in 2009 compared to 234.1mn rials in the previous year. 
				The capital adequacy ratio of banks stood higher at 15.4 per 
				cent in September 2009 as against 14.7 per cent at the end of 
				December 2008. <br>
				</font><br>
				<font color="#3778FF"><b>Is the CBO going to increase interest 
				rates in 2010 or will it continue to remain at the same level?<br>
				</b></font><font color="#333333">Interest rates are deregulated 
				in Oman (other than for personal loans) and its level is 
				determined by market forces. Interest rates charged by banks are 
				determined based on many factors such as cost of funds 
				(including equity), risk premium, operating costs, extent of 
				collaterals etc. <br>
				</font><br>
				<font color="#3778FF"><b>What is the CBO’s stand on de-pegging 
				the Omani Rial from the US dollar?<br>
				</b></font><font color="#333333">The CBO remains firmly 
				committed to the fixed peg, and our assessment has been that 
				despite the concerns arising from imported inflation, the 
				benefits of the fixed peg are substantial, given our economic 
				structure and high degree of openness in both current and 
				capital accounts. The fixed peg provides the country with a 
				stable exchange rate which helps in promoting investment, growth 
				and diversification of the economy. <br>
				<br>
				</font><b><font color="#3778FF">As oil prices go up, the threat 
				of inflation looms around the corner. Is the CBO working on 
				measures to deal with inflation?<br>
				</font></b><font color="#333333">As of now, the international 
				oil prices seems to have stabilised between $70 and $80 per 
				barrel. The recovery of the western countries appears to be 
				fragile. The global demand for oil may not shoot up unless 
				recovery gathers momentum in the major western countries. The 
				upside risks to inflation in most of the western countries are 
				modest as output gap remains negative. There is no immediate 
				threat to inflation from abroad. So far as domestic demand is 
				concerned, it is conditioned by contraction of nominal GDP which 
				has declined by 27.4 per cent upto the third quarter of 2009. 
				The average inflation decelerated to 3.4 per cent in 2009 
				compared to 12.4 per cent in the previous year. <br>
				<br>
				The CBO is keeping a vigil over the price situation in Oman. If 
				upside risks to inflation accelerate, appropriate monetary 
				measures will be taken going ahead. As of now, restoring growth 
				to the pre-crisis level is considered more important vis-à-vis 
				the inflationary pressure in the economy.</font></font></td>
