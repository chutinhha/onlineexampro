				</font><font face="Verdana" color="#333333" size="2">A look at 
				the methodology used to rank the banks in the survey</font></b><font face="Verdana" size="1" color="#333333"><br>
				We have taken growth and sustainability parameters for ranking 
				of commercial banks in Oman. The ranking was done among the 
				leading banks which include BankMuscat, National Bank of Oman, 
				Oman Arab Bank, BankDhofar, and Oman International Bank. We have 
				taken the period from 2006 to 2009 to rank them on the set 
				parameters for growth and sustainability. Ahli Bank has seen a 
				transition from housing bank to commercial bank during this 
				period because of which we have excluded the bank in our 
				ranking. For now, the newly established Bank Sohar also has been 
				excluded.<br>
				<br>
				The data was sourced from the published financial statements of 
				the banks, CBO reports and other published reports. Ranking 
				based on growth is done by calculating the CAGR growth of gross 
				loans, customer deposits, fee income, operating profit, networth 
				and net profit during the period from 2006 to 2009. Ranking for 
				sustainability was done by calculating the four year average of 
				three key factors like asset quality, productivity and 
				efficiency. <br>
				<br>
				Asset quality was ranked based on non performing assets to the 
				gross loan (NPA/GL) ratio and the overall loan provisions to non 
				performing assets (provision coverage ratio). Productivity is 
				ranked based on operating profit of the bank to its number of 
				branches and number of employees. Efficiency considers net 
				interest margins (net interest income/average interest bearing 
				assets), cost to income ratio, return on average assets and 
				return on average equity. For the calculation of cost to income 
				ratio, we have excluded the depreciation expenses. </font></td>
