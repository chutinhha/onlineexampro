                    <font face="Verdana" size="1"><br>
                    </font><b><font face="Verdana" size="4" color="#C00000">
					Stable and mature<br>
					</font><font face="Verdana" size="2">The performance of the 
					Omani banking industry underlines the soundness, efficiency 
					and resilience of the sector to external shocks. <br>
					</font><font face="Verdana" size="1"><i>Mayank Singh and 
					Visvas Paul D Karra report</i></font></b><font size="1" face="Verdana, Arial, Helvetica, sans-serif"><br>
                    </font><font size="1" face="Verdana, Arial, Helvetica, sans-serif">
					<b><br>
                    As the financial crisis fades and the banking sector faces 
					less headwinds, it’s a good time to do a status check on the 
					way fortunes of individual banks have been impacted by the 
					crisis. This year’s OER-GBCM survey throws up interesting 
					insights about the sectors compulsions, performance and 
					future direction. <br>
                    </b><br>
					Oman’s financial system is dominated by banks which account 
					for more than 90 percent of total assets and liabilities of 
					the financial sector as a whole. At the end of 2009, the 
					Omani banking system consisted of 17 commercial banks, of 
					which seven are locally incorporated and ten are branches of 
					foreign banks. In addition, there are two specialised banks 
					operating in the country, one catering to development of 
					projects and the other providing mortgage loans. <br>
					<br>
					<b>Calibrated growth</b><br>
					In Oman, the banking industry has been witnessing 
					significant growth in recent years, mainly due to sustained 
					increase in oil prices, efforts to diversify the economy, 
					implementation of large industrial, real estate and 
					infrastructure projects and growing emphasis on the private 
					sector. Total assets of the commercial banks, which were 
					4.9bn rials at the end of 2004, rose to 14.2bn rials at the 
					end of 2009, indicating an annual average growth of about 38 
					per cent during the last five years. Bank credit constituted 
					bulk of the banks’ assets which remained by and large stable 
					in the range of 65-70 per cent of the total assets. Despite 
					increase in the size of balance sheets, there has been a 
					steady decline in the gross non-performing loans as 
					percentage of the total assets fell from nearly 10 per cent 
					in 2004 to 2.1 per cent in 2008, before rising marginally in 
					2009 due to cyclical downturn of the economy. System-wise 
					capital adequacy ratio for commercial banks as percentage of 
					otal risk weighted assets also remained at a satisfactory 
					level of around 15 per cent during the same period as 
					against the minimum regulatory requirement of 10 per cent.
					<br>
					<br>
					<b>Strong framework</b><br>
					Despite knock-on effects on the banking sector from the 
					recent global financial crisis, Oman’s banking system 
					remained sound, resilient and profitable due to appropriate 
					regulatory and supervisory policy adopted by the CBO. The 
					emphasis on financial stability by the CBO has been 
					reflected in fine tuning of regulatory and supervisory norms 
					for banks from time-to-time in line with the international 
					codes and best practices. In pursuance of its commitment to 
					adopt global best practices in banking regulation and 
					supervision, the CBO was in the forefront to mandate 
					implementation of Basel II capital adequacy framework since 
					January 2007. <br>
					<br>
					The minimum capital requirement for new local banks was 
					raised from 50mn rials to 100mn rials and for foreign banks 
					from 10mn rials to 20mn rials. The CBO’s prescription of 
					general provisions of one percent on the performing 
					non-personal loans and two percent on performing personal 
					loans, introduced in 2004, has proved to be extremely useful 
					in providing much needed cushion for Omani banks to meet the 
					challenges of the global economic crisis. <br>
					<br>
					The banking industry around the world has been passing 
					through a critical phase since 2008. In 2009, most of the 
					banks and financial institutions, particularly in the 
					western countries, were busy in repairing their balance 
					sheets, infusing capital, and recovering from the state of 
					credit crunch. Despite adverse international developments, 
					Omani banks earned profits of 198.4mn rials (provisional) in 
					2009 as against 234.1mn rials in the previous year. Despite 
					slowdown of both deposit and credit growth, Omani banks were 
					in a better position to consolidate their business in 2009 
					compared to their counterparts in the region.
