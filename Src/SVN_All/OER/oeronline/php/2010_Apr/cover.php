					a strong wicket</font><font size="1" face="Verdana, Arial, Helvetica, sans-serif"><br>
					</font>
					<font size="2" face="Verdana, Arial, Helvetica, sans-serif">
					Hamood Sangour Al Zadjali, Executive President, Central Bank 
					of Oman replied to a written questionnaire sent by OER 
					regarding the issues and concerns facing the banking sector 
					in the Sultanate</font></b></td>
