					and the reasons behind his success</b></font><font face="Verdana" size="1"><br>
					<br>
					<b>When OER caught up with R Prabakaran, general manager, 
					Computer Stationery Industry, which owns the prestigious 
					commercial printing press – Oman Printers &amp; Stationers Co 
					(OPS), he was more keen to talk about the achievements of 
					his group rather than himself.<br>
					</b><br>
					</font><font face="Verdana" size="1">Anvwar Al-Balushi began 
					his career in corporate credit in the year 1994, with one of 
					the leading local banks in Oman. That was soon after his 
					graduation in commerce from Mumbai University. “I consider 
					the first break in my career happened when I got the 
					opportunity to work with ANZ Grindlays,” says Anvwar, “It 
					helped me grow my interest towards corporate banking sector 
					as a direct result of participating in several international 
					conferences, seminars, forums etc.” Having got international 
					exposure with ANZ, he chose to return to a local bank and 
					later pursued his career in the corporate finance sector. 
					His next major career breakthrough came in 2007 when he 
					joined Majan Consolidated Company as head of business 
					development and in 2009 he was promoted as the group CEO. 
					“According to me, my notable contributions to the company 
					will happen in the coming days and I am actively working on 
					many of them. However, I joined Majan in 2007 and within a 
					span of two years was promoted as the group CEO, which 
					obviously can be considered as the direct result of my hard 
					work and contribution to the company’s growth and success.” 
					This is what he had to say when asked about the reason 
					behind this success, “Success parameters differ with each 
					individual, yet for me personally one must seek to become 
					successful with a creative mind tempered with patience and 
					excellent listening skills.”<br>
					<br>
                    </font><b><font face="Verdana" size="1">Leisure hours</font><font face="Verdana" size="2"><br>
                    </font></b><font face="Verdana" size="1">Outside of work he 
					spends a lot of time participating in welfare programmes, 
					especially social causes like caring for children with Down 
					Syndrome. In fact, whenever possible he likes to spend time 
					with a few families whom he knows personally and whose 
					children suffer from the disease. The fact that the group 
					Chairman, Sayyed Barghash Said al-Said takes special 
					interest in the welfare of children with special needs has 
					encouraged Majan Consolidated to extend generous support to 
					the cause. The company organised a Down Syndrome Day for 
					children on March 21, 2009. “It will definitely bring in a 
					sea of improvement to their lives, once the general public 
					and more companies come forward with such caring gestures.” 
					A constant seeker of knowledge Anvwar’s primary pastime, 
					when not participating in welfare activities and spending 
					time at work, is reading. “I read books by many management 
					thinkers to keep myself professionally updated and to learn 
					from their experiences. Amongst the latest ones that I read, 
					my current favourites are Leadership Secrets of the World’s 
					Most Successful CEO’s by Eric Yaverbaum and The Effective 
					Executive by Peter F. Drucker.”<br>
					<br>
					An avid traveller, Anvwar loves to explore his homeland. But 
					outside Oman, his favourite destination is Malaysia. “The 
					land is very picturesque and nobody can ignore the warmth of 
					Malaysian hospitality and friendliness of its people. 
					Coupled with these, we have business partners from Malaysia 
					and naturally occasional visits happen.”<br>
					<br>
					“I owe it to my family for their support and understanding 
					that helps me balance my work and personal lives,” says 
					Anvwar. “Otherwise, it would have been very difficult for me 
					to carry out my job since professionally it is a highly 
					demanding period for me and I am sure this situation will 
					continue for a few more months owing to a couple of major 
					projects we have committed to undertake,” he concludes with 
					a smile.<br>
