				<p align="center" style="margin: 3px 10px"> 
					<b><font face="Verdana" size="4" color="#C00000">
				<img border="0" src="images/IMG_9965.jpg" hspace="9" vspace="5" style="border: 4px solid #C0C0C0; " width="80" height="138" align="left"></font></b></p>
				<p align="justify" style="margin: 3px 10px"> 
					<b><font face="Verdana" size="2">Christopher S Wilson, 
					Assistant US Trade Representative, Europe and The Middle 
					East visited Oman recently. He spoke to Mayank Singh about 
					trade relations, bilateral agreements versus multilateralism 
					and the need to resist protectionist urges<br>
&nbsp;</font></b></p>
				<p align="justify" style="margin: 3px 10px"> 
					<font face="Verdana" size="1"><b>How much progress has been 
					made on the US-Oman Free Trade Agreement (FTA)?</b><br>
					Things have been progressing smoothly, but there is a need 
					to raise awareness about the FTA. Ultimately both the 
					countries will measure progress made on the agreement based 
					on actual trade flows. When the FTA came into force, 
					bilateral trade stood at $2bn. <br>
					Our experience with FTA’s in the past is that it takes three 
					to four years before there is a big jump in trade and we 
					expect to see a similar trend in Oman. Since 2009 was a 
					difficult year, it would be unfair to judge the impact of 
					the FTA immediately. <br>
					<br>
					<b>What is the roadmap for the FTA in the years ahead?</b><br>
					Creating awareness about the FTA has been identified as a 
					focus area for the year 2010. Unless there is awareness 
					about the opportunities that the agreement has created, and 
					they are utilised the FTA will remain a mere document. The 
					other area in which the US can help is technical assistance 
					and capacity building. We have been lending support to Oman 
					in customs related matters and this will strengthen 
					bilateral trade. <br>
					<br>
					<b>There were media reports about the US wanting more 
					liberalisation of certain sectors in Oman, before the entry 
					into force of the FTA. Are you satisfied with the progress 
					made by Oman in this respect?<br>
					</b>While preparing for the entry into force of an FTA, we 
					review all the laws of that country like government 
					procurement, practices, licensing, intellectual property 
					rights and we engaged intensively in such a process with 
					Oman. It was a tough process, but now both sides are 
					comfortable with each other. We may have occasional issues, 
					but for now based on our work, we do not see any major 
					problems. <br>
					<br>
					<b>What are the main challenges that the two countries face 
					in relation to the FTA?<br>
					</b>The two countries have not had historical or traditional 
					ties and so a lot of work needs to be done to increase 
					familiarity between the two countries. The good part is that 
					both the governments acknowledge the FTA as an important 
					initiative and are ready to work hand-in-hand.<br>
					<br>
					<b>The mismatch in scales between Omani and US businesses 
					creates apprehensions about local companies ability to 
					compete in the US market. How would you respond to this?<br>
					</b>It is important for small businesses to seek a niche 
					partnership for a given product of service in the US market 
					and then to grow it. If these companies try to come in at 
					the top end of the market, then they are bound to find it 
					difficult. For information Omani companies can always come 
					to our office and we would be happy to help them. <br>
					<br>
					<b>The financial crisis has led to calls for the US to 
					become more protectionist and insulated economically. Is 
					there a rethink at the government level on these lines?<br>
					I</b>n a period of economic challenges we have to resist the 
					temptation of becoming more protectionist. A year back there 
					were impulses that wanted us to focus inwardly, but 
					President Obama and his team have resisted such pressures at 
					every opportunity. If the US becomes protectionist and other 
					countries follow, then it will slow international trade to 
					the detriment of all.<br>
					<br>
					<b>The US efforts to sign individual trade treaties with 
					various countries undermines the quest for a global trade 
					regime under the auspices of the World Trade Organisation 
					(WTO). How do you justify this contradiction?<br>
					</b>I believe that it is possible to support a multilateral 
					system while pursuing individual treaties as we want open 
					markets. The WTO has a wide diversity of interests and it is 
					difficult to come to a consensus. Bilateral treaties like 
					the FTA in fact helps the WTO’s quest for a open market 
					system, globally.</font></p>
