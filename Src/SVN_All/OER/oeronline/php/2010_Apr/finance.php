		        <td valign="top" width="100%"> 
                <p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"> 
                    <br>
                    </font><b><font size="1" face="Verdana, Arial, Helvetica, sans-serif"> 
                    <img src="images/Paul-Cooper.jpg" hspace="9" vspace="5" border="0" align="right" width="100" height="153" style="border: 3px solid #EFEFE9; "></font><font face="Verdana" size="4" color="#C00000">Upbeat 
				prospects</font></b><font face="Verdana" size="1"><br>
				</font><b><font face="Verdana" size="2"><br>
				Paul Cooper, Managing Director, Sarasin-Alpen gives a low down 
				on the regional market scenario in a candid chat with Mayank 
				Singh</font></b><p align="justify" style="margin: 3px 10px">
				<font face="Verdana" size="1"><br>
				<b>What is your take on GCC economies for the year 2010?<br>
				</b>This year is certainly going to be better than 2009. Though 
				2009 was a difficult year for GCC economies, most countries 
				weathered the financial crisis better than other parts of the 
				world. Looking ahead we expect the GCC economies to come out of 
				the downturn faster than the developed world. The financial 
				problem was once in a generation crisis and so the base effect 
				will also help results in 2010. Fiscal and monetary policy has 
				been loose and so every instrument available to central bankers 
				and policy makers have been used to tackle the problem. The 
				global economy seems to have bottomed out in the second or third 
				quarter of 2009 and it has been growing since then. <br>
				<br>
				<b>Do you expect global interest rates to remain at zero levels 
				in 2010 or are they expected to rise?<br>
				</b>If economies are growing quickly, central bankers (and 
				governments) can respond to the situation in two ways – they can 
				either raise interest rates or increase taxes and cut back 
				expenditure and they are more likely to do the latter this year 
				as they have tremendous deficits that are not sustainable. If 
				they want to slow economies they will do this with fiscal 
				policy. The recovery is not going to be so strong that it can 
				withstand higher interest rates and more taxes. So interest 
				rates will continue to be low for longer than what people 
				anticipate. <br>
				<br>
				<b>The crisis led to liquidity constraints as interbank lending 
				stopped and companies found it difficult to get credit. Has this 
				eased up now?<br>
				</b>The credit crunch has certainly eased, it was spectacularly 
				bad and it is no surprise that things have healed up. The 
				interbank market is functioning at more normal levels, if you 
				look at LIBOR rates they are trading at the normal spread over 
				official base rates, so banks have enough liquidity the question 
				now is how do they want to use this and do they want to take the 
				cyclical risk that comes with lending. Now we are facing a 
				normal cyclical downturn where as earlier we were facing an 
				unprecedented credit crunch. So the financial system is working.<br>
				<br>
				Stock markets usually factorise future growth, but most indices 
				in the region have not gone up significantly. What is your 
				reading of the situation?<br>
				That’s right, if you take out Saudi Arabia from the MSCI GCC 
				index, the other five countries have been completely flat in 
				2009. Comparatively, emerging global markets rose by 74 per cent 
				during the same period. On a global basis investors did 
				anticipate the future, but in the region the same did not happen 
				despite strong oil prices. The reason for this is that the 
				regional markets are dominated by retail investors and private 
				clients who are less willing to anticipate the future. It’s 
				usually the institutional investors who anticipate the future, 
				before the private investors and because of the dominance of 
				private investors in the region, there has been a bit of a lag 
				in the regional markets. The regional markets did not have the 
				same kind of momentum like other markets as the year came to a 
				close. The Dubai crisis too kept investors on the sidelines. But 
				they will come back as valuations in GCC markets are very 
				attractive and it is just a matter of time before which 
				international investors start buying into opportunities in the 
				regional market and start driving back some momentum. <br>
				<br>
				<b>Where do you expect the MSCI GCC index to be in 2010? <br>
				</b>It would be difficult for me to take that call, but if you 
				see most markets in the GCC, are trading 60 per cent lower than 
				what they were two years ago. The UAE index is 79 per cent below 
				the high reached five years ago. Admittedly, there was a bubble 
				element five years back, but the fact remains that these markets 
				offer tremendous value that is not available anywhere else in 
				the world. The fundamentals in the region are strong and we just 
				need the banking sector to heal itself and it will take one or 
				two quarters for that to happen and once we get into the second 
				half of the year, prospects for the region are very good. I 
				expect to be sitting here next year at the same time and looking 
				back at a very good 2010. <br>
				<b><br>
				Do you expect various countries in the GCC region to come out of 
				the slowdown in different stages? <br>
				</b>Economically, most people see Saudi Arabia and Qatar as the 
				most interesting markets in the short term. Saudi Arabia is a 
				large economy which is not dependent on world trade. Qatar is 
				experiencing a growth in its gas output and it is expected to 
				grow at over 15 per cent in real terms. There are strengths 
				elsewhere too, Oman’s prospects are quite good, not being a part 
				of OPEC helped Oman to raise its oil production in 2009 and 
				2010. UAE and Kuwait are expected to lag in the recovery 
				process. In the case of UAE it is the usual reasons and Kuwait 
				is being affected by similar but less visible reasons. The 
				country is less stable politically and there are a number of 
				financial companies there which will take longer to heal. <br>
				<b><br>
				How should investors negotiate such a market, should they trade 
				or invest for the medium or long term. Is there a particular 
				strategy to pursue?<br>
				</b>My advice would be to invest for the long term (one-to-three 
				years). There are certain trading techniques that you can apply 
				at the margin. One has to take a call on whether one wants to be 
				in the market or out of the market and anything apart from this 
				is just tinkering. The markets do trend, when they are going 
				down one should sell and when markets are moving up, there is a 
				good opportunity to buy and hold. The important thing is to read 
				the trend and not to sit in markets till they go down. The 
				danger is that people do excessive trading as they do not know 
				whether a trend has been established or not. So trading is not a 
				good thing in these markets as liquidity is less, but still one 
				needs to be active. <br>
				<br>
				<b>What are the major drawbacks of regional markets and what can 
				be done to address these concerns?<br>
				</b>The markets need more liquidity as it is difficult for 
				international investors to participate now. Even within the 
				region, a country like Oman only has a weightage of 2.6 per cent 
				on the MSCI GCC index. So if a fund manager has an index based 
				approach he would allocate only a small sum to the Omani market. 
				And if you have a global emerging markets perspective then you 
				do not put anything into these markets as none of the GCC 
				countries are a part of the MSCI global emerging markets index. 
				So there is a need to bring in more liquidity into these markets 
				so that they become larger and get a representation on global 
				indexes. A long term institutional savings culture would help 
				this process. <br>
				<b><br>
				One fallout of the crisis has been the fall from grace of 
				financial analysts, fund managers, bankers, brokers etc. Has 
				this affected the credibility of people like yourself? </b><br>
				The credibility of the industry has suffered. There is a 
				question mark over the accuracy of investor forecasts, 
				recommendations of marketing people’s presentation styles are 
				questioned. Clients have lost a degree of trust but the same 
				applies to their own investment capabilities. A number of people 
				whom I have come across have been active investors on their own 
				and they have suffered much worse. The need to have a 
				professional financial advisor is more important now than it has 
				ever been before and the financial crisis does not negate the 
				need for that.
