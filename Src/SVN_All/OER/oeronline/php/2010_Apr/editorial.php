				</b><font face="Verdana" size="1"><br>
				The financial year 2009 proved to be many things to many people 
				– difficult, a rude bolt from the blue, a lesson in moderation 
				and a reminder that yesterday’s masterstroke can abruptly 
				transform into evident folly. What the year was not for sure is 
				predictable. And that’s reflected in the rankings of the 
				OER-GBCM Best Banks Survey. New winners across categories, old 
				warhorses who’ve enjoyed the limelight for years have been 
				humbled, and those perceived to be laggards have climbed to the 
				top of the list. <br>
				<br>
				Overall, Oman’s banking sector has stood up resolutely to the 
				crisis. There was an increase in all major banking aggregates 
				despite the deceleration reflecting the cyclical downturn of the 
				economy. Provisional figures of commercial banks showed a modest 
				growth in total assets of 2.9 percent to 14.2bn rials in 
				December 2009 compared to 13.8bn rials in 2008. Total 
				outstanding credit rose by 6.2 percent to 9.8bn rials compared 
				to 9.25bn rials a year ago. Aggregate deposits registered a 
				growth of six per cent from 8.6bn rials to 9.1bn rials. 
				Provisional net profits of commercial banks stood were slightly 
				lower at 190.8mn rials in 2009 compared to 234.1mn rials in the 
				previous year. <br>
				<br>
				Talking about individual banks, BankMuscat witnessed subdued 
				growth during FY09 on the back of increased provisioning 
				requirements. It has fully provided for its exposures towards 
				Saudi Arabia’s Saad and Gosaibi groups in the last fiscal. In 
				terms of sustainability, the bank continues to maintain a good 
				performance. <br>
				<br>
				BankDhofar reported a steady growth in its credit and deposit 
				base during FY09, which in turn lead to improved ranking in 
				terms of its growth. The bank also registered higher 
				productivity and improved return ratios. NBO saw lower growth 
				during the last year along with higher provisioning requirements 
				which affected its asset quality and efficiency.<br>
				<br>
				OAB leads the charts in terms of sustainability. The bank has 
				superior return ratios and higher net interest margins as 
				against its peer group. OIB reported subdued growth compared to 
				others during last year. Though the bank has some concerns in 
				growth and productivity, it’s efficiency indicators are pretty 
				good. Ahli Bank does well in terms of growth and asset quality. 
				The same holds true for Bank Sohar. <br>
				<br>
				Banking was the first sector to be affected by the financial 
				crisis and the global recovery will inevitable be led by it. Our 
				cover story package gives you some pointers about when that may 
				be and the course that it may take.<br>
