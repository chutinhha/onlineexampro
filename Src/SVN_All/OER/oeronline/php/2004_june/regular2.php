<p align="justify"><font face="Verdana" size="4" color="#FF0000">Car 
            Review<br>
<br>
</font><b><font face="Verdana" size="2" color="#FF0000">Czech mate</font></b><font face="Verdana" size="1"><br>
<br>
<b>The Skoda brand of cars, launched in the Sultanate earlier this year, is 
doing pretty well on the Omani roads. Vicky Kapur test drives the Skoda Superb 
Comfort. </b><br>
<br>
You get a lot of car for RO9,500 these days. With the Skoda Superb, you get 25<img border="0" src="car_re.jpg" align="right" hspace="5" vspace="2" width="350" height="143"> centimetres more than an Audi A4 
- and that for a lot less Rial Omani. That 
translates into so much rear legroom that back seat passengers may complain of 
loneliness. And it gives the Superb a very elegant, limo-like look from the rear 
three-quarters. In a market segment where size does matter, the Skoda Superb is 
a perfect foil for any car in its class. Indeed, the fact that, at 4.8 metres, 
the Superb is closer in length to the Audi A6 says a lot about the Skoda's 
position in the marketplace. <br>
<br>
Launched in February this year, the Skoda models are doing pretty well on Omani 
roads. It's nice to know that while launching the brand in the Sultanate, Skoda 
were determined that their now-famous value-for-money reputation worldwide 
should not be subverted by a chance to cash in on the affluent local market. OER 
test drove the Skoda Superb Comfort with 2.8 lt tiptronic transmission, and the 
proposition is difficult to fault. <br>
<br>
Obviously, a VWAG product through and through, it shares all major components 
with its in-house siblings, along with concomitant build-quality and the 
company's generous warranties. The V6 engine (along with other available power 
units) is shared with VW and Audi. The Tiptronic box is another common component 
and the whole thing smacks of VWAG brand values - that means a solid feel, 
impressive build quality and well-engineered driveability. <br>
<br>
<b>Exteriors </b>
<br>
Even the styling, with its slightly proud grille and executive stance adds a 
kind of majesty not common in its class. Of the three nominal VWAG rivals, it 
looks the most expensive and, in styling terms at least, arguably relegates the 
Mondeo and Vectra into the play-off zone. And with six inches more legroom than 
the Passat, the Superb is far from being an optical illusion, although a number 
of BMW drivers apparently could not believe their eyes. With a 100mm stretch in 
its wheelbase over it's VW Passat relative - which it bears more than a passing 
resemblance to - the Superb is a big car. <br>
<br>
To fully appreciate the design nuances of the Skoda Superb, look particularly to 
its rear door length. There's no need for a tape measure; your eyes alone tell 
the full story. On the Superb, the rear openers are veritable elephant ears, 
being a good one-third longer than similar cars in the class. <br>
<br>
The Superb is a big car - its wheelbase stretch goes to the area behind the 
front seats, and translates into the sort of legroom you'd normally associate 
with an airport limo. In fact, it's been reckoned only a Mercedes-Benz S600 can 
better the Skoda's rear cabin space. No wonder the Czech president chose one as 
his State limo! <br>
<br>
<b>Interiors </b><br>
This is very nicely VWAG with full climate control on all models and 
Audi-like Venetian blinds to the vents, two of which feed hot or cold air from 
the centre console to those far away rear seat passengers. Surprise and delight 
features abound, with a combined boot and fuel filler keylock in the front door 
pocket, height and reach adjustable steering wheel, height adjustable front 
seats with lumbar support, and now not merely a cassette radio but a CD player 
in the dashboard. My favourite feature of the lot is inside the boot, where you 
can pull a couple of tabs to reveal four hooks from which to suspend those 
supermarket bags that normally spill their contents throughout the luggage 
locker. <br>
<br>
At the risk of sounding like the label on a sauce bottle, I am going to list the 
ingredients in a Skoda Superb. Stick with me and prepare to be impressed. Here 
goes...equipment includes twin front and side airbags; climate control with 
additional vents to the rear seats; remote central locking; height-adjustable 
front seats with lumbar supports; front and rear electric windows with 
bounce-back; an eight-speaker sound system with a CD player; a rake- and 
reach-adjustable steering wheel; electric heated mirrors; tinted glass; a 
multi-function computer; fog lights; delayed courtesy lamps - front and rear; 
leg-space illumination, a 30-second 'coming home' function for the headlights 
and boarding lamps that shine down from the exterior mirrors. <br>
<br>
The Superb is not a small person's car. At 180 cm, I'm about average, but felt 
like a midget in this motor. It actually took a long time to get comfortable, 
not helped by a lack of driver's seat thigh support (there's no tilt). But two 
six foot-sixers can actually sit behind each other comfortably. The Comfort 
model (as tested) also gets 17-inch alloys; partial leather upholstery, 
tree-wood trim, odd bits of chrome, leather-wrapped gear-lever knob and steering 
wheel; cruise control; a multi-function steering wheel; acoustic parking sensor 
and an integrated umbrella pocket, complete with a folding gamp. <br>
<br>
To all this munificence must be added a number of pragmatic qualities, which 
range from the neat and inevitably Teutonic rectilinear fascia to a host of 
storage cubby holes including an air-conditioned glove box and a sizeable boot 
that will swallow over 16 cubic feet of luggage on a platform more than a metre 
deep. <br>
<br>
<b>Performance </b><br>
The driving experience is partly familiar Passat, and partly not. 
The car I drove went as well as you'd expect a well-seasoned one to go. But the 
steering is disconcertingly centre-weighted so you can really feel the pendulum 
effect o<img border="0" src="car_re1.jpg" align="right" hspace="5" vspace="5" width="250" height="177">f the heavy engine overhanging the front wheels. But once you learn to 
drive through that, it actually clings on extremely well. You just have to be 
careful about getting tired on a highway where a long sweeping bend could see 
you lurching from lane to lane if you don't brace one of your arms holding the 
wheel. <br>
<br>
On several occasions when I was driving along the Sultan Qaboos St highway at a 
not-unreasonable pace, I found myself being tailgated by you-know-who. Thanks to 
the torque-rich 2.8-litre V6 oil-burner, mild throttle pressure, even in a high 
gear, produces instant response and I simply accelerated away until I could find 
space to pull to the right. Having caught up, they started to pass only to drop 
back for a second look as their brain registered the &quot;Superb' badge. Some shook 
their heads in what I took to be disbelief but one ventured a 'thumbs-up'. 
Approbation from the great and wonderful is not something that I generally seek, 
but I suspect a lot of future owners will enjoy the odd moment of inverse 
snobbery as occasionally the supposed underdog proves it can bite back. <br>
<br>
<b>Safety </b><br>
All models are bundled with ABS incorporating an electronic differential 
lock (EDL). All V6-powered Comfort models come complete with ESP, which provides 
braking and traction control and electronic braking assistance in emergency 
stops. The relatively longer wheelbase not only provides a slightly superior 
ride quality but the handling and road holding are not far short of the hotter 
Octavias, altogether smaller and lighter cars. This car will take some serious 
stick without stepping out of line and the only down side is a tendency to roll 
slightly on sharp bends taken at speed, arguably a function of the larger and 
heavier platform. But this is hardly significant when you consider the majestic 
grace with which it eats up highway kilometres. <br>
<br>
<b>The finishing line </b><br>
The Superb is designed to take on family-size cars such as 
the Mondeo and Peugeot 406, plus its Passat cousin from within the VW family, 
but it could also steal sales from more upmarket cars or those from the next 
size-class upwards (GM's Omega, for instance). It is based on a Passat platform 
stretched by 10cm, all in the rear seat area, and its mechanical parts are 
Passat-derived, as are the front doors and the structure - if not the detailing 
- of the dashboard. <br>
<br>
Styled like a bigger, better-proportioned Octavia, the hefty Superb has superb 
rear legroom, a high standard of finish, lots of equipment and VW quality. It's 
already the Czech governmental transport of choice, filling the void left by 
Tatra, and on a different note, the UK's police forces are already taking an 
interest. The RO9,500 question is: will Oman find Skoda superb?<br>
<br>
