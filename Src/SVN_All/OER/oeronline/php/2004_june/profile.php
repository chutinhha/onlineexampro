                    well as foreign stakeholders and the media. Having no prior 
                    experience of public speaking, he was, naturally, a bit 
                    anxious and edgy. Not one to throw in the towel in the face 
                    of challenges, the young man decided to go about the whole 
                    affair in a meticulous manner. &quot;First of all, I wrote down 
                    what I wanted to say, he reminisces. <br>
                    <br>
                    He remembers that even at that age, he was aware that it is 
                    important for a speaker to say what people want to hear and 
                    not what you want to say. <br>
                    <br>
                    &quot;The speech should be short and to-the-point. Audiences do 
                    not like long speeches. It gets boring. You should just come 
                    to the point, convey the message, and that's it,Ó he says.
                    <br>
                    <br>
                    &quot;So, I called some of my senior staff members and rehearsed 
                    it in front of them,Ó he recalls. 'It was my first speech 
                    after all, he adds with a smile. 'Only after I was sure that 
                    my staff was happy with the speech did I stop rehearsing and 
                    finally went on to deliver the speech. Needless to add, the 
                    whole episode went off perfectly. <br>
                    <br>
                    It is these very qualities - perfectionist to the core, 
                    meticulous and exacting in approach, and ever ready to face 
                    challenges - that Hassan Kamer Sultan (HKS), the director of 
                    WJ Towell &amp; Co. LLC, continues to live by. <br>
                    <br>
                    Learning the ropes HKS began his education in Oman in the 
                    early 1960s, and studied here till the highest level then 
                    available - the sixth grade. There on, he travelled to 
                    Kuwait and completed his schooling there. 'Even in Kuwait, 
                    there were no universities in the 1970s, and so I went to 
                    England to pursue my higher studies, he recounts. During the 
                    same time, HKS also completed a 'sandwich course in Business 
                    Administration - six months academic learning followed by 
                    six months of practice in a business concern. That helped me 
                    a lot, he claims, because I got to not only understand the 
                    theories, but also the opportunity to apply them in real 
                    business scenarios.&quot; <br>
                    <br>
                    Back from England in 1978, HKS started working in his family 
                    business. &quot;Initially, I had three departments under me, he 
                    informs. Those were the Mazoon Printing Press, Towell Auto 
                    Centre and the Tools Division, which included the tyres and 
                    spare parts sections as well. &quot;We were trying to expand our 
                    business then, he recalls, and I split up the division into 
                    smaller units. When we started expanding, we separated the 
                    spare parts department into an independent division. Then 
                    again, when the tyres business grew, we separated that too 
                    from the tools business. In this manner, HKS was 
                    instrumental in making the company grow and, at the same 
                    time, make each smaller unit profitable on its own. <br>
                    <br>
                    It is a tribute to his constant endeavour to make things 
                    better in his divisions that HKS is, till date, the chairman 
                    of Towell Auto Centre. HKS was also, till recently, the 
                    director in-charge of Mazoon, the first private printing 
                    press to be set up in Oman. &quot;We bought second-hand machines 
                    from the government and set up shop as early as 1966. It was 
                    also the first press to install a 4-colour machine in the 
                    Sultanate and we are renowned for the quality of our 
                    printing, he beams. <br>
                    <br>
                    HKS also put in a lot of hard work to improve and expand the 
                    operations of Towell Auto Centre. I was in charge of the 
                    Mazda franchise, he says, and did a lot of work in setting 
                    up the branches across Oman - in Ibri, Sohar, Buraimi, Sur, 
                    Ibra etc. In fact, as acknowledgement of its absolute 
                    distinction in all fields of operations, Towell Auto Centre 
                    was one of the five finalists to have been nominated for the 
                    coveted Flame of Excellence award in this year's Oman Awards 
                    for Excellence. <br>
                    <br>
                    Management mores The WJ Towell group has about 30 divisions 
                    in all, which are run by its 9 directors, who have 
                    independent charge of the departments they manage. Over and 
                    above, each director represents the company on the board of 
                    directors of the public listed companies. All the directors 
                    and board members work very closely, trying to cooperate 
                    with each other, he informs. According to HKS, Although the 
                    directors are very much involved in the business, our (the 
                    group's) policy is that we do not interfere in the 
                    day-to-day business of the managers. We have given the 
                    business heads complete freedom to look after their 
                    businesses. Of course, we watch them closely and if they 
                    have any problem, they come to us. HKS and the other 
                    directors of the group hold monthly meetings with the 
                    department heads to keep abreast of their particular 
                    businesses. At the same time, he confirms, <br>
                    <br>
                    &quot;we have regular monthly board meetings to look into any 
                    issues that the group as a whole might be facing. Our 
                    meetings are very consistent. We even invite our general 
                    managers to the board meetings to make presentations about 
                    their businesses.&quot;<br>
                    <br>
                    HKS maintains that the group's top management is very open 
                    and friendly with the various divisions' general managers. 
                    Our policy is to respect them and, at the same time, make 
                    them feel like winners. We don't pressurise them, he 
                    insists. In fact, according to HKS, the managers have a 
                    share in the group's success. We keep them aware that if, as 
                    a result of their efforts, the company makes profits, they 
                    will share the benefits in terms of bonuses, promotions and 
                    travelling to the principles for training etc. So, they have 
                    a share in the companies' growth.&quot; <br>
                    <br>
                    The group also encourages and motivates their senior 
                    managers by regularly reviewing their progress and rewarding 
                    - monetarily and otherwise - everyone who works hard. Says 
                    HKS: &quot;We believe that employees are the best assets for a 
                    company, and we give them the due respect. We have employees 
                    who have been with us for a very long time.&quot; <br>
                    <br>
                    Awards &amp; accolades Apart from being nominated for the 
                    highest private sector honour - the Flame of Excellence - 
                    this year, the group's various companies have received a lot 
                    of international awards and recognition. Towell Auto Centre 
                    has received awards for being the agents of Mazda for the 
                    last 30 years. We have also received the Best Distributors 
                    in the Middle East award from Bridgestone Tyres, and have 
                    received a host of awards for Mazda services besides being 
                    the Best Distributors in the Middle East for Castrol 
                    Lubricants, he claims. <br>
                    <br>
                    All these honours are over and above the routine perks that 
                    being top performers for global brands like Bridgestone and 
                    MARS chocolates etc. entail. HKS has been to Wimbledon to 
                    watch tennis as well as to Bahrain to watch the Formula 1 
                    courtesy the brands he handles. His managers have even been 
                    to Morocco on quasi-official visits thanks to MARS 
                    chocolates. <br>
