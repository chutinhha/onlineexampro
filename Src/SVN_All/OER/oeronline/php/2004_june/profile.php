                    Personal profile A father of three, HKS is in fighting fit 
                    shape, playing one-and-a-half hours of vigorous tennis 
                    thrice a week at the Al Bustan Palace Hotel. In fact, he has 
                    even won one of the internal tennis championships hosted by 
                    the hotel. One has to try to be younger, longer, he 
                    proclaims. Playing tennis is a sort of relaxation and 
                    socialising opportunity as well, he adds.<br>
