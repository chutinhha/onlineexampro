<p align="justify"><font face="Verdana" size="4" color="#FF0000">
            Banking &amp; Finance<br>
<br>
</font><b><font face="Verdana" color="#FF0000" style="font-size: 14pt">
Bancassurance: </font><font face="Verdana" size="2" color="#FF0000"><br>
Coming soon to a bank near you...</font></b></p>

<p align="justify"><font face="Verdana" size="1"><b>There is a good potential 
for Bancassurance growth in Oman and its neighbouring countries. </b><br>
<b>Ali Hassan Moosa looks at some of the issues that need to be addressed prior 
to embarking on the journey </b><br>
<br>
</font><font face="Verdana" style="font-size: 11pt"><b>A</b></font><font face="Verdana" size="1">cross 
the globe, one of the most significant changes to the financial services 
industry in the past few years has been the development of Bancassurance. Banks 
and insurance companies have found the opportunity attractive in terms of 
increasing market share, margins and boosting profitability. <br>
<br>
Bancassurance as a term first originated in France signifying the convergence of 
banking and insurance. It is defined as selling insurance products through bank 
network. While it developed into a success story in many parts of the world, 
Bancassurance is relatively new in Oman. <br>
<br>
Banks aim to sustain their competitive edge by offering value-added products and 
services. As a result, customer loyalty is retained. Insurance products bestow a 
healthy contribution towards a bank's non-interest income. Insurers, on the 
other hand, gain access to larger market share by using a bank's diversified 
clientele and branch networks. The Bancassurance route provides insurers with an 
incentive to develop financially oriented products in order to gain bigger 
market penetration. <br>
<br>
Although many banks across the world have chosen to sell all types of insurance 
covers including auto insurance, some banks have decided to offer financially 
oriented insurance covers that easily cross-sell with banking products. Examples 
include individual or group life, disability, long-term care, fixed annuities, 
equity indexed annuities, estate planning, investment trusts, retirement 
planning and child education. <br>
<br>
Currently in Oman, insurance products sold through banks are credit related. 
Examples include life cover for all types of personal loans and property cover 
where real-estate collateral exists. In case of death or disability, the 
insurance company prepays the outstanding loan balance so that the surviving 
members of the family are not burdened with the loan. <br>
<br>
The degree to which banks devote themselves to the sale and servicing of 
insurance varies from country to country and among individual banks. 
Bancassurance has been successful in many parts of Europe, but specifically in 
France, where as much as 50 per cent of life insurance policies are sold through 
banks. The pioneer was a regional bank called Credit Mutuel who created its own 
life and non-life insurance subsidiaries in the early 70s. In the UK, 
Bancassurance started in 1985 when Standard Life, an insurance company, 
purchased shares in Bank of Scotland. Soon thereafter, legislative changes took 
place allowing banks to market all types of insurance products as well as 
allowing banks to set up insurance subsidiaries. <br>
<br>
Unlike Europe, the US has witnessed limited success despite promoting and 
encouraging Bancassurance through the Financial Services Modernization Act of 
1999. Banks mostly sell simple and inexpensive life insurance policies. Another 
unique trend in the US has been premium financing. Wealthy individuals sometime 
borrow money to pay for expensive life policies. Although the US life industry 
does not track premium financing sales figures, some industry insiders believe 
that close to $1 billion per annum in new life insurance business is sold 
through this method. <br>
<br>
A recent study conducted by 'Swiss Re', a major world-class re-insurer, showed 
that Bancassurance penetration is expected to increase in Asia over the next 
five years, potentially accounting for 13 per cent of life policies and 6 per 
cent on other insurance policies. Furthermore, it is expected that one-third of 
these estimates represents new business rather than customer shifting from 
existing delivery channels to banks. <br>
<br>
In other parts of the world such as Latin America, financial analysts widely 
believe that conditions are favourable for rapid growth of Bancassurance. 
Economic, social and political conditions all favour such growth expectations.
<br>
<br>
There is a good potential for Bancassurance growth in Oman and its neighbours. 
Another survey conducted by 'Swiss Re' a couple of years back (see table below) 
presented a comparison between insurance penetration of some GCC countries and 
other developed economies. The development of Islamic related insurance products 
(&quot;Takaful&quot;) in neighbouring countries will undoubtedly augment growth 
potentials. <br>
<br>
Examples of joint insurance and bank ventures in the GCC for distribution 
agreements include National Bank of Kuwait (NBK) and American Life Insurance Co. 
(ALICO) in Kuwait, Mashreqbank and Oman Insurance Co. in the UAE, and Oman 
International Bank (OIB) and Muscat Insurance Co. in Oman. <br>
<br>
Locally, insurance companies and their agents are regulated by the Ministry of 
Commerce &amp; Industry. It is widely believed in the market that in due course, 
insurance regulation will be moved to another government body (favourite bet is 
the Capital Markets Authority). Until the matter is sorted, the uncertainty does 
create anxiety among insurers and bankers alike, slowing down the development of 
Bancassurance. <br>
<br>
The banking regulator, the Central Bank of Oman (CBO), had issued some 
guidelines with respect to Bancassurance. The approach taken by the CBO so far 
can be best described as cautionary. For obvious reasons, the banking regulator 
does not wish banks to unduly expose themselves to insurance related risks. <br>
<br>
Other than exclusive regulatory authority for insurance companies, the majority 
of insurers (and bankers), however, are hoping for more. Bancassurance 
regulations need to be more pragmatic whereby insurance companies and banks are 
able to create commercially viable synergies. The consumer must also secure a 
fair deal in the process. <br>
<br>
There is no single &quot;best&quot; approach for integrating Bancassurance. Theoretically, 
Banks and insurers have a choice of three broad structures (not mutually 
exclusive) to choose from:<br>
<br>
</font><font face="Marlett" size="1">h</font><font face="Verdana" size="1">A 
distribution agreement between bank and insurer. This structure itself has two 
modes:<br>
---&gt; Insurer using bank premises to sell insurance products. It has been evident 
though that the model of placing insurance employee in bank to sell insurance 
products may not be commercially viable. Additional bodies create all sorts of 
human resource issues. <br>
<br>
---&gt; Agent/Brokerage model: Bank employees are trained to cross-sell insurance 
products. On the face of it, the model is more lucrative. It allows a multitude 
of tie-ups but it is also tougher to implement. Currently, the agent model is 
what is allowed by local regulation.<br>
</font><font face="Marlett" size="1">h</font><font face="Verdana" size="1">A 
joint venture in which bank and insurer have a common ownership in a joint 
venture insurance company.<br>
</font><font face="Marlett" size="1">h</font><font face="Verdana" size="1">An 
arrangement in which a holding company owns both the bank and the insurance 
firm.<br>
<br>
It may be worth mentioning that a couple of years back, a major bank in the 
Sultanate had an in-principle agreement to acquire major shareholding of a local 
insurance company. However, and to the dismay of its shareholders, the final 
deal was blocked by regulators. <br>
<br>
Branding is an important issue to address. Will insurance products be branded as 
bank products or in the insurer's name? In order to cross-sell successfully, the 
answer should really be driven as per customer expectations. <br>
<br>
There are also customer service issues that need to be worked out. A third-party 
arrangement could mean that banks would leave inquiries and customer service to 
the insurance company; yet, banks like to keep customers in hand and deal with 
them directly. As a result, it may be wise that simpler insurance products are 
sold through banking channels and more complex ones are left for insurers. 
Training bank staff to cross-sell insurance products is imperative. It is in the 
best interest of insurance companies to invest wisely in training bank staff 
rather than relying on banks to conduct their own training programs. Banks may 
also note the reputation and legal exposures should their staff present 
inaccurate product information to customers. <br>
<br>
Prior to embarking on the Bancassurance journey, interested parties in Oman 
should learn form the experience of others. We need to develop Bancassurance 
gradually and start by selling simple bank-customer oriented insurance products 
for a fee or commission. At this early stage, insurance risks ought to be fully 
transferred to principle insurers (and in turn to their re-insurers). Once 
expertise is built and customer acceptance is gained, other Bancassurance 
opportunities could further be explored. <br>
