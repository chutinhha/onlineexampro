            </font><font face="Verdana" size="4">You've reached <br>
            world-class mobility...</font></p>
            <p align="justify"><font face="Verdana" size="1">
            <b>Bahrain now has two competing operators in mobile telephony: Batelco and MTC-Vodafone, and is leading the GCC states in terms of 
            competitiveness in the sector.<br>
            <br>
            <center><img border="0" src="table_bahrain.jpg" width="392" height="186"></center></b>
            </font>
            <p align="justify"><font face="Verdana" style="font-size: 11pt">
            <b>B</b></font><font face="Verdana" size="1">atelco - the Bahrain 
            Telecommunications Co - held the monopoly in telecommunications 
            provision in the country since it was established in 1981, until 
            last year. The government of Bahrain owns 36 per cent of Batelco. 
            Another 20 per cent is owned by Cable &amp; Wireless (UK), with which 
            Batelco has strong technical links. The remaining shares are listed 
            on the Bahrain stock exchange. Batelco introduced digital mobile 
            telephone services in Bahrain in 1995. MTC-Vodafone is a partnership 
            created in 2002 between Kuwait's Mobile Telecommunications Company (MTC), 
            and the international telecommunications company Vodafone (UK). The 
            partnership with Vodafone gives MTC access to Vodafone's 
            international mobile services. MTC owns 60 per cent of MTC-Vodafone 
            Bahrain and Bahraini investors own the remainder. <br>
            <br>
            MTC was established in 1983 and has been a dynamic driver of 
            telecommunications in Kuwait. It is now marketing itself vigorously 
            in other parts of the Gulf, having successfully bid for a mobile 
            licence in the southern part of Iraq. The company is now contending 
            for mobile licences in both Saudi Arabia, Oman and also the UAE. 
            Crown Prince Sheikh Salman bin Hamad Al Khalifa, Commander-in-Chief 
            of the Bahrain Defence Force, announced the end of Batelco's 
            telecoms monopoly in February 2001. Sheikh Salman has been 
            personally overseeing the opening up of the telecom market and the 
            transition to a regulated environment for telecoms competitors 
            together with transportation minister Sheikh Ali bin Khalifa Al 
            Khalifa. <br>
            <br>
            Speaking in February 2001, Sheikh Salman said, &quot;We are against all 
            monopolies as the island is opening up its market to pave the way 
            for solid growth. In fact, the decision to end this monopoly and 
            open up the telecom market was taken after the National Action 
            Charter was submitted on December 23 (2000).&quot; He declared that the 
            government would open the telecom market to all, including 
            international companies. <br>
            <br>
            </font><font face="Verdana" style="font-size: 11pt">
            <b>T</b></font><font face="Verdana" size="1">he government set up 
            the Telecommunications Regulatory Authority (TRA) under Legislative 
            Decree No. 48 of 2002 promulgating the Telecommunications Law, to 
            oversee the liberalisation process. The TRA is an independent body 
            and its duties include, among other things, protecting the interests 
            of subscribers and users and promoting effective and fair 
            competition among existing and new licensed operators. <br>
            <br>
            The TRA issued the invitation to apply for the second mobile licence 
            in Bahrain on January 1, 2003. Ten companies vied initially for the 
            licence. MTC-Vodafone Bahrain beat its rivals, Kalaam and Wataniya 
            Telecom Bahrain, to win the licence on April 20, 2003. The process 
            of issuing the licence was managed by Intercai Mondiale Ltd. 
            Incentives for the bidding companies included an upfront licence fee 
            of $265,000 (RO102,000), a modest annual licence fee of 1 per cent 
            of gross revenues against the minimum 10 per cent in other Arab 
            countries, unbundling of Batelco's ducts and fibre infrastructure, 
            national roaming with Batelco for a year, the option but not the 
            obligation, to rollout 3G services and the right to lease circuits 
            from Batelco for International Long Distance service provision. <br>
            <br>
            MTC-Vodafone's new mobile service began in December 2003. The 
            company has a 15-year concession to offer GSM services for local and 
            international calls. The company has decided to exercise the option 
            to develop 3G technology, and has disclosed plans to invest $120 
            million (RO46.15 million) in Bahrain. In order to ensure fair play, 
            the TRA has warned of consequences for anti-competitive behaviour, 
            notably price-fixing between the Batelco and MTC-Vodafone Bahrain.
            <br>
            <br>
            </font><font face="Verdana" style="font-size: 11pt">
            <b>T</b></font><font face="Verdana" size="1">he Economic and Social 
            Commission of Western Asia determined that Bahrain had the fourth 
            highest mobile phone penetration of all its member states in 2003. A 
            report published in 2002 by the Arab Advisors Group revealed that, 
            despite the small population of the island (740,000), the market for 
            mobile telephony in Bahrain was far from sated. Bahrain was 
            projected to have a GSM subscriber penetration of 77 per cent by 
            2006, an increase from a figure of approximately 43 per cent in 
            2001. <br>
            <br>
            According to an Arab Advisors Group report, Bahrain has a more 
            diversified economy than other Gulf states, and wealth is more 
            evenly distributed. Thus, there is more scope for post-pay pricing 
            plans in the country, in contrast to other countries in the region, 
            where the depth of mobile phone penetration has been largely due to 
            the take-up by low-income, first-time mobile users. Authors of the 
            report anticipated that future average revenue per subscription (ARPS) 
            in Bahrain could be boosted by a higher rate of post-pay 
            contributions compared with other Gulf States. The chart below 
            suggests that, along with UAE and Kuwait, Bahrain has a much higher 
            mobile penetration amongst lower-income inhabitants than does Oman, 
            Qatar or Saudi Arabia.<br>
            <br>
            </font><font face="Verdana" style="font-size: 11pt">
            <b>N</b></font><font face="Verdana" size="1">evertheless, a review 
            of tariffs being offered by both Batelco and MTC-Vodafone suggests 
            that both companies continue to target the pre-paid market. 
            Batelco's simSim and MTC-Vodafone's eeZee pre-paid plans offer 
            similar tariffs, with eeZee taking the edge perhaps on its eeZee 
            Extra plan in terms of price per minute. However, Batelco has 
            launched new, lower-price international calling rates to India, 
            Pakistan, Philippines, Sri Lanka and Bangladesh. Batelco also cut 
            mobile phone rates by up to 35 per cent in 2003. The market has been 
            flooded by cheap handsets from Asia, which makes mobile telephony 
            affordable to a peripatetic expatriate population. <br>
            <br>
            The other market segment to be targeted by the mobile companies is 
            the growing 14-20 year age group, for whom the ability to download 
            ring tones, chat and exchange multi-media messages as well as SMS is 
            important. <br>
            <br>
            MTC-Vodafone Bahrain launched its chain of shops in January this 
            year, which are designed to give its customers a taste of what its 
            3G mobile service can do. COO Khalid Al Farkh said that, &quot;The shop 
            is set out to maximise opportunities for interaction between 
            customers and the MTC-Vodafone service team. MTC-Vodafone has 
            introduced consultation desks, Ôdownload' zones, Ôfeel at home' 
            areas and dedicated areas for Ôbusiness' users, waving goodbye to 
            the traditional customer service offer of queues, tickets and 
            waiting.&quot; Following MTC's lead, Batelco announced in February that 
            it would open more of its own shops with the same customer-friendly 
            approach.<br>
            <br>
            </font><font face="Verdana" style="font-size: 11pt">
            <b>F</b></font><font face="Verdana" size="1">irst quarter figures 
            since introduction of its service show that MTC-Vodafone Bahrain has 
            already captured 10 per cent of the mobile phone market. An 
            international gateway licence to be awarded in July 2004 will enable 
            the company to provide cost-competitive, state-of-the-art 
            international connectivity to its Bahrain customers. <br>
            <br>
            Last month, On May 9, the Bahrain government announced that it plans 
            to make a strategic exit from its shareholding in Batelco, valued at 
            200 million Bahraini dinars ($530.5 million). Mahmoud Kooheji, 
            director of public shareholdings at the Ministry of Finance and 
            National Economy, said that the government may sell its stake in the 
            company to &quot;a UK-based telecom giant&quot;. In 2002, Batelco's largest 
            revenue stream was provided by mobile telephony, accounting for 38 
            per cent of the company's overall gross revenue for the period. 
            Total revenue in 2003 increased by 3 per cent to BD192.1 million. 
            Net profit, before appropriations, rose by 5 per cent, reaching 
            BD60.7 million at the end of 2003. This amounts to a 22 per cent 
            return on average capital employed during 2003. <br>
            <br>
            The next few years are likely to see a shift in the market. 
            Customers will be looking for value-added services if they are to 
            consider exchanging their mobile phones or phone packages. They will 
            need to be wooed by loyalty plans, discounts and promotions, 
            creative offers, good service, reliability of the networks and 
            greater content and flexibility. Operators who rise to this 
            challenge will be the ones who will survive. <br>
            <br>
