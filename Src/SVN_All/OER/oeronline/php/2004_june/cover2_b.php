            <p align="justify"><font face="Verdana" size="1">
            <b>Pressure from WTO members and pent-up demand locally has forced 
            Saudi Arabia, GCC's biggest mobile telephony market, to open its 
            door to foreign investors <br>
            <br><center>
            <img border="0" src="table_s_a.jpg" width="388" height="186"></center></b>
            </font>
            <p align="justify"><font face="Verdana" style="font-size: 11pt">
            <b>A</b></font><font face="Verdana" size="1">lthough a large market 
            in terms of population, in growth terms, the telecom sector in Saudi 
            Arabia lags behind neighbouring GCC countries. Despite its 
            considerable commercial potential, private investment is presently 
            allowed in only a few minor sub-sectors. However, telecom services 
            remain one of the most dynamic sectors in Saudi Arabia. Since its 
            founding as a public company in 1998, Saudi Telecommunications 
            Company (STC) has been carrying out major telecoms projects 
            countrywide, taking over some of the functions previously subsumed 
            within the former Ministry of Post, Telephones &amp; Telegraphs. The 
            renamed Ministry of Communications &amp; Information Technology (MCIT) 
            is seeking to facilitate the deregulation of the industry and to 
            create a second telecom operator. Part-liberalisation of the mobile 
            telephony market is expected to be achieved by the fourth quarter of 
            2004, with one new mobile operator set to be licensed. At present, 
            the mostly State-owned STC enjoys a highly profitable monopoly. <br>
            <br>
            Saudi Arabia, which is aspiring to become a member of the World 
            Trade Organisation (WTO), has been facing pressure from other 
            members to liberalise its mobile and fixed line telephony market. 
            WTO members had also demanded that other telecom-related fields be 
            liberalised. The first step taken by the Kingdom in this regard was 
            licensing of four new data service operators in late 2003. These 
            companies were only allowed to offer services via VSAT (very small 
            aperture terminal) networks, and were not expected to have much 
            impact on STC's hold on this section of the market. <br>
            <br>
            However, a time plan has been drawn up for phased liberalisation of 
            other fields. But the impending second GSM licensing will be the 
            biggest move yet, affecting STC monopoly. The country has an 
            estimated 22.5 million people, with 86.5 per cent of the population 
            living in urban areas, and some 6.3 million of working age. There 
            has been considerable growth in the telephone network since 1997. 
            There were around 3.9 million fixed telephone lines at the end of 
            2002, equivalent to 16.6 per cent of the population, compared with 
            about 12 per cent in 1998. <br>
            <br>
            Mobile subscriber growth has been strong, but penetration is low 
            compared with other countries in the GCC and other parts of the 
            wider Middle East. Currently, there are around 7 million 
            subscribers, including post- and pre-paid market segments, amounting 
            to a mobile penetration rate of 30 per cent. <br>
            <br>
            Says Mohsen Malaki, program &amp; consulting manager (Communications), 
            IDC CEMA: &quot;Due to the impending liberalisation of the mobile 
            services market, STC is expected to focus on retaining and expanding 
            its market share through streamlining of costs, improvement of 
            service offerings, and targeted tariff reductions.&quot; STC has already 
            adapted its marketing strategy to handle competition from a second 
            mobile operator in 2005. <br>
            <br>
            </font><font face="Verdana" style="font-size: 11pt">
            <b>T</b></font><font face="Verdana" size="1">he STC is the dominant 
            service provider for all communications markets in the country. Its 
            business can be split into two categories: wireline and wireless. 
            Wireline revenues comprise principally fixed-line telecom services 
            but also include leased circuits, telex/telegraph and Internet 
            services. Wireline services generated revenues of SR10.6bn 
            (US$2.8bn) in 2002, down from SR11.8bn in 2001. The wireless 
            business comprises mainly mobile telecom services, but also includes 
            mobile roaming and pager services. Wireless services generated 
            revenues of SR12.9bn in 2002, up from SR8bn in 2001. <br>
            <br>
            STC's mobile network covers 92 per cent of the population in Saudi 
            Arabia. The mobile services are divided into post- and pre-paid 
            services. Since 1998, STC has invested more than 34.5 per cent of 
            its total capital expenditure to expand its mobile network in terms 
            of geographic coverage, increase capacity in high traffic and demand 
            areas, and upgrade its infrastructure to enable the provisioning of 
            new services. STC president Khalid bin Abdullah Al Molhem has said, 
            &quot;The entry of a second GSM player is a welcome development, that we 
            have anticipated and been preparing for since several months. We 
            foresee several benefits arising out of this situation for industry 
            players and customers alike. The industry is currently growing at an 
            average rate of 30 per cent annually, and we are confident that a 
            new player will expand the market, and drive further growth, rather 
            than restrict STC's market share.&quot; The more than three times 
            oversubscribed IPO in STC occurred in December 2002, a company whose 
            shares continue to dominate the local stockmarket and whose profits 
            have continued to increase firmly ever since. <br>
            <br>
            </font><font face="Verdana" style="font-size: 11pt">
            <b>S</b></font><font face="Verdana" size="1">audi Arabia has 
            selected eleven consortia as candidates for its second cellular 
            license. Operators that are actively pursuing the tender include 
            UK's Vodafone, Egypt's Orascom Telecom and Kuwait's MTC. An 
            unspecified number of 3G licences are also expected to be issued 
            between 2006 and 2007, with a mobile virtual network operator 
            licence provisionally set to be issued at an interim stage. The 
            monopoly on fixed-line domestic telephone services should end by 
            2008. <br>
            <br>
            Although STC has shed its status as an arm of the government, by 
            investing in rapid expansion of its mobile-phone market and 
            producing healthy financial results, there is still plenty of 
            untapped potential in the market. Saudi's tele-density (total 
            subscribers per 100 inhabitants) was only 36.1 per cent according to 
            the International Telecommunications Union. This compares 
            unfavourably with 110 per cent in UAE, 85 per cent in Bahrain and 72 
            per cent in Kuwait. <br>
            <br>
            With Saudi Arabia officially removing mobile telephony off the list 
            of activities barred to foreign investors, the rate is expected to 
            shoot up. &quot;The penetration of the telecom sector in Saudi Arabia is 
            less than the international average, which indicates that there is 
            strong potential for growth in the Kingdom. We expect industry 
            growth to reach $7.9 billion by 2007,&quot; Al Molhem has said. The 
            second licensee, in this scenario, is expected to have a dual focus. 
            &quot;The new entrant is likely to leverage the expected high 
            subscription growth rate in the early years of its launch by 
            endeavouring to attract a large number of new customers via prepaid 
            services,&quot; Malaki has said. &quot;The new entrant will also attempt to 
            churn high-spending subscribers from STC in order to avoid being 
            pigeonholed as a low ASPS (average spending per subscription) 
            operator.&quot; <br>
            <br>
            </font><font face="Verdana" style="font-size: 11pt">
            <b>I</b></font><font face="Verdana" size="1">n 2001, foreign 
            investment and competition in the telecom sector was approved, while 
            a regulatory body, the Saudi Communications Commission, was set up 
            in June 2001 Ð now called the CITC. According to new regulations, to 
            bid for a mobile licence, each consortium had to include at least 
            five Saudi companies, and also an operating company that is 
            committed to owning at least 15 per cent of new licensee, if and 
            when Saudi laws permit such investment during the licensing period. 
            It is also not clear if the option of national roaming is being 
            offered to the new entrant. To counter this, the second operator has 
            to quickly build its infrastructure to expand its geographical reach 
            Ð this is crucial if it hopes to attract high-end subscribers away 
            from STC. <br>
            <br>
            </font><font face="Verdana" style="font-size: 11pt">
            <b>E</b></font><font face="Verdana" size="1">ver since the CITC 
            announced its plan to liberalise, STC has been focusing on rapid 
            subscriber acquisition through the introduction of its prepaid 
            service. Even before the second licensee begins operations, 
            customers have begun enjoying the benefits of healthy competition. 
            The pre-paid service, for instance, benefited the lower-spending 
            subscribers and customers without credit history, by lowering the 
            entry barrier. Activation fees and deposit requirements have also 
            been lowered. STC is now expanding direct and indirect sales and 
            distribution channels and introducing innovative services. Massive 
            investments are also being made to develop better infrastructure.
            <br>
            <br>
            According to Molhem, the immediate impact of liberalisation would be 
            a drop in prices in the face of increased competition. Molhem had 
            placed particular emphasis on company-customer relations and 
            affirmed that the company's financial performance would continue 
            through growth in operational revenues and profits. STC's net profit 
            for the first quarter of 2004 rose by 102 per cent to SR2.26 billion 
            ($603 million). The president attributed the huge growth in revenues 
            to the increase in the number of subscribers to STC services. It has 
            made huge profits from sales of pre-paid mobile phone (SAWA) cards, 
            of which 300,000 units are estimated to be sold every month. <br>
            <br>
