            <p align="left"><font face="Verdana" size="1">
            <b>With the Sultanate expected to award any time now the second 
            mobile licence to ring in competition for the State monopoly player, 
            Omani mobile subscribers await a whole new era<br>
            <center>
            <img border="0" src="table_oman.jpg" width="391" height="182"></center></b>
            <p align="justify">
            </font><font face="Verdana" style="font-size: 11pt">
            <b>O</b></font><font face="Verdana" size="1">man's Public Switched 
            Telephone Network (PSTN) and mobile markets are still monopoly 
            markets operated by the government-owned Oman Telecommunications Co. 
            (Omantel) and the newly-formed Oman Mobile Telecommunications 
            Company (OMTC), the first telecom firm licensed to provide mobile 
            telecommunications services in Oman. The government's efforts to 
            privatise the country's telecommunications services led to the legal 
            and organisational establishment of Omantel in mid-1999, which 
            replaced the previous State-owned monopoly General 
            Telecommunications Organisation. <br>
            <br>
            The Sultanate, through the Telecom Regulatory Authority (TRA), 
            initiated the process to award a second public mobile licence with 
            the objective of introducing competition, which in turn is expected 
            to benefit individual consumers and society as a whole through 
            competitive and reasonable prices, better services and expanded 
            consumer choice. Mohammed bin Ali Al Wohaibi, the executive 
            president of Omantel, who is also the chairman of the board of 
            directors of OMTC, explained that the incorporation of OMTC was 
            aimed at developing and enhancing the mobile telecommunications 
            service in the Sultanate in light of the impending competition. <br>
            <br>
            The establishment of OMTC followed the issuing of two licences in 
            February 2004 for Omantel Ð the first for providing basic fixed 
            general telecom services for 25 years and the second for providing 
            basic mobile telecom services for 15 years. <br>
            <br>
            </font><font face="Verdana" style="font-size: 11pt">
            <b>T</b></font><font face="Verdana" size="1">he TRA initially 
            received 28 applications for the licence to establish a private firm 
            to set up a second mobile telephone company in the country and, 
            according to reports in regional newspapers, in April 2004, it 
            shortlisted six regional and international companies. Government 
            officials named the companies in the race as UK's Cable &amp; Wireless 
            bidding jointly with Bahrain's Batelco; Mobile Telecommunications 
            Company of Kuwait bidding along with four Omani partners Ð WJ Towell 
            &amp; Co., Mohsin Haider Darwish, Jawad Sultan Technology and Oman 
            Holdings International; Mobilkom of Austria; Qatar's Q-Tel bidding 
            with Denmark's TDC; Investcom Holding of Luxembourg; and Turkey's 
            Celtel International along with Vodafone/Mobile Systems 
            International. <br>
            <br>
            While the winner was expected to be announced in May, technical 
            delays pushed back 
            <img border="0" src="wom1.jpg" align="right" hspace="5" width="150" height="165">plans to award the licence; however, officials 
            said that the country was determined to name a successful bidder by 
            the end of May. The announcement, though, was yet to be made till 
            the time OER went to Press. The proposed opening up of the sector 
            will also increase private investment in the sector as well as 
            accelerate expansion of infrastructure to un-served and underserved 
            areas of the country. <br>
            <br>
            TRA member Colonel Mohsin Al Hafeedh, who is also director-general 
            of information technology at Royal Oman Police, said that opening up 
            of the telecom market for competition, transferring the management 
            and operations of telecom services to the private sector for ending 
            monopoly and ensuring access to essential telecom services for all 
            citizens regardless of their financial position or geographical 
            locations were among the highpoints of the government's 
            reform-drive. Currently, the TRA has three temporary members Ñ 
            Colonel Mohsin Al Hafeedh, Eng. Naashiah Saud Al Kharusi and 
            Mohammed Abdullah Al Mahrouqi. <br>
            <br>
            </font><font face="Verdana" style="font-size: 11pt">
            <b>T</b></font><font face="Verdana" size="1">he policy framework in 
            place for the country's telecom sector also aims at promoting 
            infrastructure development that will increase tele-density in the 
            Sultanate. The current telecommunications penetration in Oman has a 
            long way to go. According to the International Telecommunications 
            Union, phone density in Oman in 2002 stood at nearly 9 per cent for 
            fixed lines and 12.4 per cent for mobile. Also, there are only 458 
            Internet users per 10,000 inhabitants. These figures point to a host 
            of opportunities for new operators in the Sultanate. <br>
            <br>
            In terms of population, only 10 per cent of individual people have a 
            fixed line while mobile users amount to just 23 per cent. In terms 
            of households, only 52 per cent of residences in the country have 
            subscribed to the telephone service. These figures reveal that 
            market penetration in Oman is the lowest in the GCC countries and 
            much lower than international standards, which translates into the 
            new service provider having a big business opportunity. <br>
            <br>
            </font><font face="Verdana" style="font-size: 11pt">
            <b>T</b></font><font face="Verdana" size="1">he growth trends in 
            subscriber base in the country's mobile market have shown a 
            remarkable increase from a mere 15,000 post-paid subscribers in 1996 
            to 235,662 in 2003. The pre-paid subscriber base stood at 358,278 in 
            2003. Based on similar regional and international trends, cellular 
            revenues in the Sultanate are expected to exceed PSTN revenues in 
            the next five years as voice-based GSM services become the growth 
            engine in the market. Omantel attributed the rise in its net profits 
            (RO104.75 million in 2003 compared to RO94.8 million the previous 
            year) to higher subscriptions to mobile telephones, which made up 
            nearly 50 per cent of its income last year. Its total revenues rose 
            by 10 per cent to RO211 million in 2003 up from RO192.8 million a 
            year earlier. The company increased its capital to RO75 million from 
            RO50 million last year. <br>
            <br>
            As for the cellular segment, a major boom in the GSM subscriber base 
            occurred in the year 2001. The subscriber base has been growing 
            steadily ever since and has exceeded the fixed line subscriber base. 
            Analysts project Oman's cellular penetration rate to exceed 50 per 
            cent in 2008, corresponding to more than 1.5 million subscribers.
            <br>
            <br>
            </font><font face="Verdana" style="font-size: 11pt">
            <b>T</b></font><font face="Verdana" size="1">alking about the 
            regulatory landscape and the investment rules and regulations in the 
            mobile sector in Oman, Eng. Naashiah Al Kharusi, member, TRA 
            committee, told OER: &quot;Any foreign or national entity wishing to 
            conduct business in Oman or to acquire a share in the capital of an 
            Omani company must obtain the approval from the Ministry of Commerce 
            &amp; Industry as provided for in the Foreign Capital Investment Law, 
            1994. This rule applies to companies intending to conduct business 
            in the telecom sector (as well). <br>
            <br>
            </font><font face="Verdana" style="font-size: 11pt">
            <b>A</b></font><font face="Verdana" size="1">ccording to TRA 
            officials, OMTC will be obliged to interconnect with the new 
            services provider upon reasonable requests. It will have to pay a 
            licence fee of RO500,000 plus annual fees besides coughing up a 
            royalty of 12 per cent of gross revenue each year, which is the same 
            for the new player too. The new player will be granted the licence 
            for the same term as OMTC (15 years). According to the TRA, unfair 
            cross-subsidisation will be strictly prohibited. &quot;The TRA will be 
            monitoring the activity of both the incumbent and the second mobile 
            operator to ensure there is no anti-competitive behaviour in the 
            areas of interconnecting, pricing, etc.,Ó says the TRA's Kharusi.
            <br>
            <br>
            According to the TRA's Kharusi, &quot;Currently, only one mobile licence 
            will be awarded. The licensee will have an exclusivity period of 
            three-and-a-half years whereby the TRA will not award another mobile 
            licence during this time. After the exclusivity period, the TRA will 
            study the market to determine if another mobile operator is 
            required.Ó According to the TRA, among the merits of a second mobile 
            licence is the fact that the roll-out plans of the new player will 
            be solely determined by the investor's business plan. Along with 
            cellular mobile services, the scope of new licence will include 
            calling card services, information services and value-added 
            services.<br>
            <br>
            </font><font face="Verdana" style="font-size: 11pt">
            <b>T</b></font><font face="Verdana" size="1">he second mobile 
            operator will be required to undergo an IPO (initial public 
            offering) within five years from the issuance of the licence. The 
            operator will have to issue 40 per cent of its shares on the Muscat 
            Securities markets,Ó says Kharusi. When queried about the services 
            that the mobile subscribers can expect from the incumbent as well as 
            the new player, Kharusi revealed that &quot;The TRA is following a 
            technology-neutral approach; hence it is a ÔMobile' licence and not 
            a ÔGSM' licence. Calling it a mobile licence keeps the options open 
            for the incoming operator to implement any technology they find 
            appropriate and does not narrow the options to GSM technology.Ó If 
            3G is introduced, the licensee will be entitled to a licence of 
            equal terms with other licensees, claim TRA officials. <br>
            <br>
            The Sultanate's major emphasis is directed towards providing 
            reasonable level of protection to its production and services 
            sectors against the strong liberalisation tendencies of the WTO, 
            with their consequent possible dumping in the local markets with 
            imported low cost goods, which may unfairly hamper the marketing of 
            local goods. Oman's economic direction is in line with the 
            contemporary trends of trade and investment liberalisation, which 
            are adopted by the WTO. <br>
            <br>
            </font><font face="Verdana" style="font-size: 11pt">
            <b>T</b></font><font face="Verdana" size="1">here is enormous scope 
            for new telephone service providers if they come in with lower and 
            attractive telecom tariffs. In fact, consumers can expect OMTC to 
            further reduce its tariffs in the immediate future, as the new 
            service provider would be entering the market with competitive 
            rates, probably lower than OMTC's. Under the universal services 
            obligation policy of the government, basic voice telephony and data 
            services are expected to be introduced to 200 new villages. <br>
            <br>
            According to a country report from the Arab Advisors Group, the 
            average monthly revenue per user (ARPU) is expected to drastically 
            decline to $20 (RO7.7) by 2008, a drop of almost 50 per cent from 
            $39 (RO15) at the end of 2002. The incumbent OMTC is in the process 
            of launching new and innovative services in the country prior to the 
            impending competition. OMTC recently selected LogicaCMG, in 
            association with Siemens Mobile Networks, to provide a Multimedia 
            Messaging Service (MMS) solution to enable the country's corporate 
            and private subscribers to exchange multimedia content across OMTC 
            and international networks. <br>
            <br>
            While justifying the introduction of the service, Dr Amer Awadh Al 
            Rawas, the managing director of OMTC, said: &quot;Mobile users in Oman 
            are quick to adopt the latest mobile technologies and our ongoing 
            priority is to keep one step ahead of their demands, not only in 
            terms of the services available, but also in the quality of service.
            <br>
            <br>
            OMTC also recently unveiled a new 100,000-line GSM exchange at Ibri 
            in the Dhairah region of the country last month. Dr Rawas said the 
            new GSM exchange would provide more capacity with greater expansion 
            potentials in the future in the wilayats of Dhairah region and the 
            adjacent villages, noting that the 27 GSM booster stations in Ibri, 
            Dhank and Yanqul which had earlier operated through Nizwa exchange 
            will now be transferred to the Ibri exchange, thus creating more 
            capacity in the Nizwa exchange and improving the services in Dhairah 
            region. The aim, he insisted, is to streamline the telecom services, 
            address any possible congestions problems in these regions and ease 
            any network pressure. <br>
            <br>
            </font><font face="Verdana" style="font-size: 11pt">
            <b>T</b></font><font face="Verdana" size="1">he Omani economy stands 
            to benefit from the planned opening up of the telecommunications 
            sector. The process will guarantee another source of income to the 
            treasury, thus help bridging the shortfall in financing. The 
            projected 2004 budget points to a RO500 million ($195 million) 
            deficit on revenue of RO2.92 billion and spending of RO3.42 billion. 
            The treasury will generate revenue from the proposed partial 
            privatisation of Omantel as well as taxes and fees levied on the 
            second mobile operator. Also, the IPO will add to the activity of 
            the Muscat Securities Market (MSM). This could rekindle memories of 
            outstanding achievements of the past years. Moreover, proliferation 
            of the mobile services will strengthen support services including 
            mobile repairing. Needless to add, the new operator will create new 
            job opportunities for locals. Import of telecom equipment such as 
            mobile phones and other items has been under the scrutiny of TRA 
            since July 1, 2003. Accordingly, importers of telecom equipment have 
            to be registered with the authority. <br>
            <br>
            </font><font face="Verdana" style="font-size: 11pt">
            <b>F</b></font><font face="Verdana" size="1">urthermore, it is 
            likely that competition will hasten the introduction of 3G 
            technology. In Bahrain, for instance, the new mobile operator, 
            Mobile Telecommunications Company (MTC) of Kuwait, in partnership 
            with Vodafone of the UK rather than Batelco, the long-serving 
            monopoly, commenced the 3G service. In Kuwait, the arrival of 
            Watanyia as the second mobile operator forced MTC to alter its 
            policies, which included looking for opportunities abroad. Confirms 
            the TRA's Kharusi: &quot;The TRA expects the new operator to add value to 
            the current telecom market with the technology provided in its 
            services. <br>
            <br>
            </font><font face="Verdana" style="font-size: 11pt">
            <b>O</b></font><font face="Verdana" size="1">n the basic fixed 
            general telecom services front, analysts expect Omantel to remain 
            the monopoly operator for fixed services until after 2005. In 2003, 
            Omantel's fixed line subscribers dropped by close to 2 per cent. 
            Oman's anticipated privatisation of the State-owned fixed line 
            operator followed by the expected liberalisation of the PSTN market 
            is expected to result in a modest increase in the growth in the 
            number of PSTN subscribers in Oman, as pockets of unmet demand 
            remain in the country. <br>
            <br>
            The Ministry of Finance plans to offer 30 per cent of 
            government-owned Omantel in the form of an IPO. Liberalisation of 
            the telecom sector dates back to March, 2002, when the government 
            decided to open up the domestic telecom market. Then, in August that 
            year, the TRA was formed, an independent regulatory body, partly to 
            oversee the privatisation of Omantel. Originally, the government 
            intended to keep 51 per cent of Omantel and offer 40 per cent to a 
            strategic partner and 9 per cent to institutional investors. But 
            there was little if any foreign interest in becoming a strategic 
            partner for Omantel. Thus, the offer was all but withdrawn. The 
            authorities had hoped that the prospective partner would expand the 
            telecom infrastructure. <br>
            <br>
            </font><font face="Verdana" style="font-size: 11pt">
            <b>F</b></font><font face="Verdana" size="1">ive consortia, led by 
            local institutions such as BankMuscat, Oman Arab Bank (OAB), Oman 
            International Bank (OIB), National Bank of Oman (NBO) and Global 
            Financial Investment Company, are in the race for the financial 
            advisory services of Omantel's much-publicised privatisation 
            programme. BankMuscat has joined hands with HSBC, the British bank 
            and Fincorp, a brokerage and investment house in Oman. OAB, a 49 per 
            cent associate of Jordan's Arab Banking Corporation, has teamed up 
            with French bank BNP Paribas and Atlas Investment Group of Jordan, 
            the investment arm of Arab Bank. OIB, the second largest bank in 
            terms of capital, has tied up with the American bank Citibank and 
            Bahrain's Security &amp; Investment Company. While NBO has tied up with 
            Gulf Investment Services, the name of the partner of the fifth 
            consortium led by Global Financial Investment Company is not 
            available. <br>
            <br>
            For a comprehensive evaluation of bids, it is expected that the 
            Tender Board, as part of reviewing the proposals, may seek 
            consultation or opinion from indigenous or international bodies, as 
            privatisation, disinvestment and initial public offering of a 
            telecom company is happening in Oman for the first time in the 
            history of Oman's telecommunication sector. <br>
            <br>
