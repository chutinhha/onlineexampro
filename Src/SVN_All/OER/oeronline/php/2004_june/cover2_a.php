                    Cover Story</font><p align="center"><font color="#FF0000" face="Verdana"><b>
            <font style="font-size: 16pt">Sultanate of Oman </font></b><font size="4"><br>
            </font></font><font face="Verdana" size="4">Call Waiting!<br>
