            our fascination with technology has made us far more demanding. This 
            attitude of wanting it all, the greatest technology dressed in the 
            finest accoutrement has existed for much longer in the exclusive 
            world of watchmakers. Connoisseurs of fine timepieces will argue 
            that no matter how skilled a jeweller is at embellishing the casing 
