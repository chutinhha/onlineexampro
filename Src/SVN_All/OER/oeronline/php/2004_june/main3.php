            power plants, the engine compartment is stark matte black plastic 
            with a neat little medallion of engine protruding in the middle with 
            all the more impressive performance statistics engraved in clear 
            sigh, lest you get asked the question by an envious friend whom you 
            are giving the grand tour. <br>
            <br>
            Needless to say, there are no user serviceable parts under here. But 
            then again, how many Bentley owners spend their weekends under their 
            cars tinkering? If, however, you do find yourself riffling through 
            the glove compartment for the owner's manual, don't bother. Simply 
            press the help button below the LCD screen and search the index 
            digitally. Unless, of course, the nature of your problem is 
            electric, in which case you will have to revert from software back 
            to treeware! <br>
            <br>
            The GT has a curious shape as if it prematurely made it off the 
            prototype pages of designers, it has a futuristic feel of 
            simplicity. Its leading edge resembles a cross between an Audi TT 
            and an 'S' type Jag after inserting a signature Bentley grille. The 
            cabin space is once again reminiscent of the TT due to its long low 
            windows that wrap around the passengers at eye level. Being a much 
            bigger car than the TT, it loses the overly rounded feel and 
            stretches out gracefully but without excessive overhang at the 
            extremities. <br>
            <br>
