            elongated tails. Today's GT traces of these features embossed on its 
            side like an old crest on a new blazer. The rear end tapers slightly 
            and is as trademark 'Bentley' as the mesh grille. E=mc2 Revised 
            Einstein's theory of relativity states that the faster an object 
            travels, the larger it becomes. This object is faster than any of 
            its predecessors, yet smaller too. Once inside Bentley, connoisseurs 
            will feel right at home although it's a much more snug fit. The 
            steering wheel seems rather large in this setting with its 
            pillow-size airbag (I wonder if it is softer than in other cars). 
