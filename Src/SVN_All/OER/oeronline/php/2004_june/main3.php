            movement inside is a marvel of mechanics and miniaturisation. Such 
            connoisseurs will scoff at jewellers who have transformed themselves 
            into watchmakers arguing that a truly great timepiece must strive 
            for perfection from the inside out. <br>
            <br>
            Given the global trend of consolidation in which exclusive luxury 
            marques are being swallowed by mass producing global giants, in a 
            world of ever larger corporations where the US of A may conceivably 
            be superseded by the UC of E (United Corporations of Earth), 
            compromises have been made in the name of profits. But not here, at 
            the very summit of automobile exclusivity, where customers put down 
            deposits before they know how much their car is going to cost. <br>
            <br>
            There is a place... a place where Bentley's Continental GT was born. A 
            car with a distinguished and a thoroughbred lineage, where speed and 
            comfort were not mutually exclusive... A place... Enough flowery talk of 
            how it all came to be, let's get down to business. I know that after 
            a half hour introduction in the showroom, I was itching to become 
            one of the first people in the Middle East to test drive this 
            fascinating new car. <br>
            <br>
            I was handed the keys, which I dropped in my pocket as I sat behind 
            the wheel. One foot on the brake and one finger on the starter 
            switch and the GT awoke with a tenorous roar. The deep bass sound 
            that emanated from the car at idle may attract many more successful 
            rap artists to this legendary name. (Will Smith and P Diddy have 
            written their Bentleys into the lyrics of their platinum selling 
            albums and started a veritable trend among rap stars.) <br>
            <br>
            <b>Sound system </b><br>
            The GT's growl is so low pitch yet loud that you find yourself 
            giving the accelerator pedal little half-inch kicks that make the 
            car shift down and jump up a few octaves. Then, seconds later, when 
            the computer figures out you aren't planning to really do anything, 
            it shifts back up to low grumble mode. In short, if you are a real 
            poser and you feel the looks of this Bentley are a little too 
            understated, you can always get people's attention by playing tunes 
            on the W12 (the name given to the power plant). You can't really 
            hear any of the higher pitch sounds because by then you are going so 
            fast they have all been swept away (unless you are in a tunnel). <br>
            <br>
            Pull the gear lever back to D then S (for Sport, I assume) and the 
            handbrake automatically disengages as you pull away. Unfortunately, 
            I was going to sit in traffic for a while before reaching a nearby 
            mountain playground of two- to four-lane ecstasies. Five hundred and 
            fifty ponies and 650 Nm of torque gnashing at the bit, but nowhere 
            to go. When I finally did get to the first stretch of open road (the 
            climb toward Al Bustan Palace) I didn't give in to the temptation of 
            simply flooring it, instead I gradually unleashed power in a manner 
            more akin to what I imagined a typical Bentley driver to be. <br>
            <br>
            The one thing I immediately noticed was this car's ability to mask 
            true speed. This is the ideal car for gentlemen with cautious wives 
            who protest when faced with the boyish tendencies of their husbands 
            to be seduced by the thrill of speed. Indeed, the GT at 200k is as 
            composed as most cars are at the speed limit. Back to the test 
            drive, and we have forked to the right and are now climbing the 
            Qantab road. It's four lanes wide and climbs steeply while curving 
            left then right with no repose. I have switched the suspension from 
            level 1 (comfort) to level 4 (sport) using the large circular dial 
            below the LCD. The GT doesn't loose its composure as it climbs 
            through the S curve. Beneath the immaculate wood and hide, there is 
            a flurry of activity. Three computer controlled systems are working 
            simultaneously; a four-wheel drive system that distributes power to 
            wherever it is most needed, a traction control system that rations 
            power when rubber threatens to loose grip and an active suspension 
            that manages that impeccable composure. The 550 horsepower twin 
            turbo power plant delivers power very smoothly. In fact, I could 
            hardly feel the turbochargers at all. There is no violent power at 
            all unless you manually select a low gear and initiate the violence 
            yourself by kicking the pedal mercilessly. Taking the GT around a 
            roundabout while pushing it, deliberately trying to unsettle it 
            produces mainly a lot of tyre screeching. <br>
            <br>
            The traction control takes this 2.5 tonne ultra luxury sports car 
            around corners both assertively and comfortably. I would love to 
            know what it would feel like if one could shed a tonne-and-a-half of 
            weight at this point. However, I am certain that a lighter composite 
            version of this type of car just wouldn't have the same solid feel 
            and if you did loose control hitting the curb, it would be a lot 
            more violent if you were at the wheel of a lighter car. <br>
            <br>
            Meanwhile in the cabin, my senses aren't picking up anything more 
            than the new car smell and the bass rumblings of the exhaust system. 
            As the road straightens out, I drop the windows, one of my favourite 
            details here is the total lack of B pillars making the side window 
            stretch the full length of the cabin. This neatly brings me to the 
            back seats, which surprisingly are perfectly acceptable for a 2x2. 
            Before setting off, I deliberately jumped in the back seat after 
            adjusting the driver's seat to my liking and, surprisingly I had 
            space for my legs. This was even more surprising when I popped the 
            trunk open and saw a deep (370 litre) cavity. <br>
            <br>
