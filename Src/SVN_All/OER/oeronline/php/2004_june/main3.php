            creation is original, subtle, compact, powerful (subtly). It sounds 
            fantastic and delivers in all departments where a GT is expected to 
            deliver. Grace and athleticism in a package labelled 'M' rather than 
            'XL' and beauty... I leave to the eye of the beholder. n</font></p>

