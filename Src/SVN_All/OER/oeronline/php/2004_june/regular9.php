            evaluated by a board for the division. The board meets once a 
            quarter as all professionally run companies do, to measure the 
            performance and progress. <br>
            <br>
            The distinction is that while the market in Oman is relatively 
            small, the effort is on innovation. You've got to continuously 
            innovate to get noticed. This is not so true in India because in a 
            mass market, you can get away with just operational efficiencies. 
            Secondly, it is a relatively low-volumes market as compared to 
            India, and you have to manage your costs and profitability very 
            carefully. There is very little margin for error. <br>
            <br>
            <b>What difficulty, typical to Oman, do you face while growing the 
            home and business solutions market?</b> <br>
            From a business point of view, I find that Oman is transiting, but 
            in projects and particularly to a lesser extent products and 
            services, there is a great emphasis on price. I am worried that the 
            price-quality trade-off could start hurting businesses overall. I 
            hope that it is a phase, which will pass, but is a difficult process 
            when you have to invest a lot in educating the people why you charge 
            a premium. The smaller players operate on a price advantage and 
            basically limit their scope of operations to trading. The long-term 
            players tend to invest in people, better quality services, 
            after-sales support etc., all of which come at a cost. The issue is 
            one of participating and positioning your company in the market 
            place, where you will be appreciated for the quality of service you 
            provide while being competitive (not necessarily cheap). Over time, 
            one hopes that the customer will clearly appreciate the benefits of 
            a quality product/ service and understand the reasons for paying the 
            premium. <br>
            <br>
            <b>Which advertising gurus do you favour and why? </b><br>
            I had the good fortune of meeting David Ogilvy in 1979-80 when he 
            came to India. I had the opportunity to hear him, meet him and talk 
            to him... so clearly he is one person who caused a very strong 
            impression on my mind. The second - because I had a hands-on working 
            experience with him - was Alyque Padamsee. The common learning that 
            I have imbibed from these two is customer-orientation. The closer 
            you are to a customer and the better you know him, the stronger the 
            ability to provide higher levels of satisfaction. The second 
            learning was that both of them propagated one common message: 
            simple, effective communication is the key to success. Being 
            advertising veterans, they have communicated their beliefs in a 
            passionate way. <br>
            <br>
            David Ogilvy was a copywriter, and his simplicity was in the words 
            that he selected. Alyque was a visualiser, and he operated through 
            the picture system. Both of them advocated the use of a 'single 
            idea' to communicate. The advertisements they created were powerful, 
            very emotive, and provoked strong customer reactions. These are 
            strong learnings from these two gentlemen - keep the ideas simple, 
            very focused, and talk to the consumer directly. I try to imbibe 
            this learning in my day-to-day operations. I like spending a lot of 
            time talking to customers and encouraging my employees to do the 
            same. <br>
