            <b>What does your role in the Zubair Corp entail?</b> <br>
            I have been with the Zubair Group for the past four years. As the 
            group general manager of Home and Business Solutions Division, I am 
            responsible for Business International Group, Photocentre LLC and 
            Zakher International. My job is to run and manage these divisions in 
            line with the group's vision. I provide strategic inputs in order 
            for the units to meet their business objectives. Photocentre is a 
            consumer products and services division, whereas Business 
            International Group is a projects business providing laboratory 
            equipment/education software and storage solutions. These are two 
            diverse businesses - the commonality being the quality of service 
            that we offer. I wear the hat of a business head, working with the 
            GMs in developing products and services, adding value where 
            possible. <br>
            <br>
            <b>How has the home &amp; business solutions landscape changed over the 
            last five years?</b> <br>
            The environment has changed, mainly driven by technology. Five years 
            ago, we hardly had any obsolescence to manage - now we have a 
            significantly high level of it. Secondly, the competitive scene has 
            become more aggressive. There are a lot of me-too brands coming in. 
            The fight is in providing distinctiveness and uniqueness in the 
            marketplace to have an edge over the other guy. The third is that 
            retailing is undergoing a significant change, particularly in Oman. 
            Five years ago, you had small retailers spread out throughout the 
            country. Today, with the arrival of the likes of Carrefour and Lulu 
            hypermarket, the retail scene is undergoing a significant change.
            <br>
            <br>
            <b>At what stage is Oman as far as state-of-the-art business 
            solutions are concerned?</b> <br>
            If you're familiar with a product lifecycle curve, you know that it 
            moves up in the initial phases and stabilises once the product/ 
            market matures. The cycle starts with innovators (people who try out 
            the product/service for the first time), and then you have early 
            adopters (people who adapt to the product/service a little later), 
            and then the movers (mass consumers). It's at this stage that the 
            product theoretically plateaus off. Using the product lifecycle 
            analogy, I think Oman is at the Early Adopters stage. Hopefully, a 
            few years from now, the market will not only mature but much larger 
            consumer franchisee of each economy will grow. <br>
            <br>
