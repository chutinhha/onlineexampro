            marketing process. This happened in the late 1970s, when I joined 
            MRF Tyres as advertising manager. Two years after I joined MRF, the 
            company got restructured, moving from tyre commodity position to a 
            customer segmented position. In this context, I took over as group 
            product manager for two- and four-wheelers. That moved me even more 
            securely into the marketing process. Since then, I've never looked 
            back. From MRF, I went on to the TTK group, and then to Wrigley's 
            India, where my last assignment was as its managing director. That's 
            how I migrated from advertising to marketing. <br>
