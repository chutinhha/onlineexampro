            Wrigley's was set up as a standalone factory at a time when the 
            government of India did not allow the import of consumer products. 
            By the time the factory was set up and we started production, import 
            liberalisation took place. So there was a bit of conflict because 
            the grey market also emerged almost in parallel with our production 
            capacity. <br>
