            </font><font face="Verdana" style="font-size: 11pt">
            <b>T</b></font><font face="Verdana" size="1">he Chinese dragon is becoming increasingly thirsty. This year,
            <img border="0" src="oil_gas.jpg" align="right" hspace="5" vspace="2" width="250" height="199">China will become the world's second biggest oil consumer after the 
            US, overtaking Japan. China will consume more than 6 million of the 
            80 million barrels the world will burn each day. That's still way 
            behind the US: Uncle Sam will gulp in excess of 20 million b/d. But 
            rampant economic growth in China is the main source of new demand, 
            sparking panic on global markets. <br>
            <br>
            The International Energy Agency (IEA) fanned the market flames in 
            mid-May when it published its Monthly Oil Market Report. The report, 
            one of the most eagerly-awaited documents on the energy calendar, 
            raised the Agency's 2004 oil demand forecast by a healthy 330,000 
            b/d, to 80.6 million b/d. That's more than 2 million b/d more than 
            last year - the highest leap for 16 years. <br>
            <br>
            The Paris-based IEA couched the announcement in typically 
            jargon-infested terms. 'The increase reflects synchronous global 
            economic expansion. First quarter estimates were revised higher as 
            OECD transport fuel use soared. Q1 demand growth in China is now 
            seen at almost 1 mb/d.' But the message was clear: the world needs 
            more oil. <br>
            <br>
            The announcement came less than 48 hours after US government's 
            Energy Information Administration (EIA) revealed that US inventories 
            of oil-based products were falling. The combination of surging 
            demand in the world's number two oil consumer - and falling 
            inventories in the number one consumer - is apt to drive prices 
            higher at the best of times. And these are far from the best of 
            times. Throw in turmoil in the Middle East, and the impact on oil 
            trading was inevitable: the price of West Texas Intermediate topped 
            $40 (RO15.4) per barrel (/b) for the first time since 1991, at the 
            height of the Gulf War. <br>
            <br>
            And then there's OPEC. Last month, OER argued that OPEC had made a 
            major blunder by cutting its export quota from April 1, to 23.5 
            million b/d. &quot;OPEC's policy was beginning to look ridiculous,&quot; we 
            wrote (OER, May 2004, pg 68). We argued that OPEC doves - 
            particularly Kuwait and the UAE - were right to argue against the 
            cut at a time of high prices. But hawks in Riyadh and Tehran got 
            their way, and the cuts went ahead. <br>
            <br>
            By early May, they were furiously back peddling. Saudi Oil Minister 
            Ali Naimi urged OPEC to raise its production ceiling by 1.5 million 
            b/d at its forthcoming meeting on May 3. 'It is certain that the 
            Kingdom believes that increasing the OPEC production ceiling is 
            essential to keep supply and demand balanced,' he said in a 
            statement. He said that increase should 'not be less than 1.5 
            million barrels a day.' Al Naimi admitted that recent price spikes 
            were a &quot;major concern&quot; to Saudi Arabia. &quot;We do not want to see 
            prices rise to a level that they negatively affect the growth of the 
            international economy or the demand for oil.&quot;<br>
            <br>
            Pity, then, the likes of Kuwaiti oil minister Sheikh Ahmad Al Fahd 
            Al Sabah, who's been saying this for months - only to be brushed 
            aside by heavyweight neighbours claiming to know better. Sheikh 
            Ahmad spent much of March being told by Al Naimi and his cohorts 
            that a production cut was vital to prevent a collapse in the oil 
            price. Sheikh Sabah listened for a while, but by May, his patience 
            had evidently worn thin. Kuwait announced that it would ignore its 
            OPEC quota of 1.8 million b/d, boosting output to its maximum 2.4 
            million b/d. &quot;I think all members are trying now to [reach the] full 
            capacity of their production,&quot; said Sheikh Sabah. <br>
            <br>
            OPEC's secretary general, Indonesian Minister for Energy and Mineral 
            Resources Purnomo Yusgiantoro, was left to be the voice of 
            moderation. He called for increased quotas - while stressing that 
            OPEC was already boosting supply, and pointing out that many factors 
            outside OPEC's control had combined to send WTI above $40/b. 
            &quot;Everybody in OPEC is concerned with high oil prices and will do as 
            much as they can to bring the price down… One of the OPEC policies 
            that can contribute to this thing is to increase the quota.&quot; <br>
            <br>
            He added: 'The main problem with the recent high prices is linked to 
            geopolitical uncertainties, inadequate refining capacity in the US 
            to cope with rising demand, multiple specifications for gasoline by 
            different states, and heavy speculation on oil by investment funds.&quot; 
            OPEC may have cut its export quota to 23.5 million barrels a day on 
            April 1, but Mr Yusgiantoro pointed out that actual production was 
            25.5 mb/d. <br>
            <br>
            What happens next? OPEC ministers meet again in Austria early June, 
            when presumably they will reverse the decision they took to cut 
            quotas on April 1. OPEC has come out of this saga with little 
            credit. The Chinese dragon shows little signs of losing its appetite 
            for crude - Q1 growth was more than 9 per cent. OPEC members will 
            have to show themselves far more adept at hitting their own oil 
            price target of $22-$28/b if they are to regain the trust of the 
            international energy community. <br>
            <br>
            <i>The author is director of Middle East Analysis, a Dubai-based 
            economic consultancy</i></font></p>

