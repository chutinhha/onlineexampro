	<tr>
		        <td valign="top" width="100%"> 
				<p align="justify" style="margin: 3px 10px"> 
					<b><font face="Verdana" size="2"><br>
					</font><font face="Verdana" size="4" color="#C00000">
				<img border="0" src="images/jun_oiandgas_img1.jpg" hspace="9" vspace="5" style="border: 2px solid #C0C0C0; " width="144" height="177" align="right"></font><font face="Verdana" size="2">Oman 
					Oil Company has been creating world-class assets and 
					facilitating industrialisation of the country. HE Maqbool 
					bin Ali Sultan, Minister of Commerce &amp; Industry, and 
					Chairman of Oman Oil Company speaks to Sunil Fernandes of 
					Oil and Gas Review on the company’s thrust areas, 
					investments and more. Excerpts:</font></b></p>
				<p align="center" style="margin: 3px 10px"> 
					&nbsp;</p>
				<p align="justify" style="margin: 3px 10px" dir="ltr"> 
					<font face="Verdana" size="1"><b>Could you elaborate on Oman 
					Oil Company? </b><br>
					Oman Oil Company (OOC) is a commercial company, wholly owned 
					by the government of the Sultanate of Oman. The company was 
					incorporated to pursue investment opportunities in the 
					energy sector both inside and outside Oman. Through 
					participation in energy and energy related projects, the 
					company plays an important role in the Sultanate’s efforts 
					to diversify the Omani economy and promote Omani and foreign 
					private sector investment. In the initial years, OOC 
					concentrated only on the oil and gas sector. However, we 
					gradually diversified into energy related business.<br>
					<br>
					OOC’s present portfolio of investments include one, 
					exploration and production projects, namely: Mukhaizana 
					concession, Oman Offshore concessions 18 and 41, Abraj 
					Energy Services, Kareem Small Fields, Rima Small Fields, 
					Dunga Concession (Kazakhstan), and Caspian Offshore 
					Concession (Kazakhstan). Second, Petrochemical companies 
					namely: Oman India Fertiliser Company, Oman Polypropylene 
					Company, Aromatics Oman LLC, Takamul Investment Company, 
					Salalah Methanol Company, La Seda Barcelona (Spain), Qingdao 
					Lidong Chemical Co. (China). Third, refining and marketing 
					companies namely: Oman Refineries and Petrochemical Co, 
					Bharat Oman Refineries Limited (India), MOL (Hungary), Oman 
					Trading International (UAE), and Oman Oil Marketing Company. 
					Fourth, energy infrastructure companies namely: Oman Gas 
					Company, Oiltanking Odjfell Terminals &amp; Co., Compania 
					Logistica de Hidrocaburos (Spain), Enagas (Spain), Planta de 
					Regasification de Sagunto (Spain), China Gas Holding 
					(China), Qingdao Lixing Terminals (China). Five, shipping 
					companies namely: Oman Shipping Company, Gulf Energy 
					Maritime (UAE). Six, power companies namely: GS Power 
					(Korea); Orient Power Company (Pakistan) and seven, a metal 
					project namely in Sohar Aluminium.<br>
					<b><br>
					What is the ratio of investments within the Sultanate and 
					outside? </b><br>
					Presently, OOC’s investments are balanced between Oman and 
					abroad.<br>
					<b><br>
					Will the ratio be maintained?</b><br>
					Investment decisions cannot be restricted. For example, if 
					we have an attractive proposition, either in Oman or abroad 
					we are going to pursue such opportunity.<br>
					<br>
					<b>The year 2009 may have been a good year to acquire 
					strategic assets, as valuations were attractive due to the 
					global recession?</b><br>
					In 2009, OOC continued to evaluate opportunities both inside 
					and outside Oman and was successful in concluding certain 
					prudent investments, adding significant value to OOC. These 
					investments include Bharat Oman Refineries Limited (BORL) in 
					India where we have increased our equity interest to 26 per 
					cent; acquisition of five per cent equity stake in Spain’s 
					leading natural gas transportation, re-gasification, and 
					storage company namely, ENAGAS; signing of a Farm Out and 
					Joint Operating Agreement with Reliance Exploration and 
					Production under which Oman Oil Company Exploration and 
					Production acquired a stake of 30 per cent and 25 per cent 
					in blocks 18 and 41 offshore Oman respectively.<br>
					<br>
					We have also finalised the major terms on a Greenfield 
					project in Uzbekistan, apart from initiating and/or 
					advancing significant investment opportunities within the 
					Sultanate.<br>
					<br>
					<b>What’s your focus for 2010 in terms of markets and 
					sectors?</b><br>
					In 2010, we are focusing on new opportunities related to our 
					business such as mining, upstream, logistics/storage, 
					pipeline, power and petrochemicals. Also, we are assessing 
					the possibilities to optimise, improve assets efficiency, 
					and expand our existing assets such as, Oman India 
					Fertiliser Co, Sohar Aluminium Co, Oman Refineries and 
					Petrochemicals Company. As to the markets, Oman enjoys 
					excellent relationship with all countries around the globe. 
					Hence, if we find any opportunities across the world, 
					meeting with our investment criteria, we are ready to pursue 
					it. Actually, there are no constraints on markets and 
					sectors.<br>
					<br>
					<b>Has there been a change in the management of companies 
					where Oman Oil Company acquires a stake?</b><br>
					OOC plays the management role through Board representation 
					in the investee companies both inside and outside Oman. 
					While exercising this role, we try to optimise the interest 
					of the investee company which entails increasing benefit to 
					the shareholders.<br>
					<b><br>
					What is OOC’s outlook in relation to taking some of the 
					subsidiary companies into IPO?</b><br>
					We have Oman Oil Marketing Company listed on the Muscat 
					Securities Market. In the next five years, we might look at 
					some of our companies launching initial public offerings and 
					subsequently getting listed. Facilitating industrialisation 
					of the Sultanate and growing assets through strategic 
					investments remains our objective.<br>
					<b><br>
					An IPO would mean that you could dilute your stake?</b><br>
					It is not a matter of diluting the stake; instead it is 
					about providing the public with an opportunity to 
					participate in our business activities. In today’s business 
					environment, it is not a matter of control but making a 
					successful business case is more important.<br>
					<b><br>
					Are there any examples where you have sold your stake 
					completely or partially?</b><br>
					Being an investment company, from time to time, we buy and 
					sell assets. However, I must point out that our objective is 
					long term investments.<br>
					<b><br>
					What is the current value of the holdings of Oman Oil 
					Company?</b><br>
					It will always be difficult to quantify the exact value of 
					holdings on daily basis. For example, the equity shares of 
					listed companies would change with the fluctuation in share 
					prices. All indications lead to the fact that the market 
					value of our assets is satisfactory to our shareholders.<br>
					<b><br>
					What would be the kind of budgets you receive for acquiring 
					assets?</b><br>
					OOC is funded though its internal resources by means of 
					return earning and by the direct funding from the government 
					and so far we haven’t encountered any difficulties for 
					investing in viable projects. <br>
					<b><br>
					Last year you formed the Oman Oil Company E&amp;P. What was the 
					objective?</b><br>
					As an international practice, when governments grant oil and 
					gas concessions, the government’s interest in the project is 
					assigned to a government vehicle. In Oman as well, when MOG 
					grants any concessions the government’s interest is assigned 
					to OOC, which is one of the investment arms of the 
					government.<br>
					<br>
					We are thankful for the confidence reposed in OOC by the 
					government. OOCEP has been created with the objective of 
					managing the existing upstream assets and to pursue new 
					investment opportunities in the E&amp;P sector both inside and 
					outside Oman.<br>
					<br>
					<b>The Ministry of Oil and Gas has been talking of granting 
					as many as 11 concessions this year. Would Oman Oil Company 
					take any stake in such blocks?</b><br>
					We will study each opportunity and take decisions 
					accordingly.<br>
					<b><br>
					You talked of downstream; would that mean that you would be 
					interested in projects like the Duqm Refinery?</b><br>
					As a matter of fact OOC along with International Petroleum 
					Investment Company of Abu Dhabi (IPIC) is doing a detailed 
					feasibility study on the plans for the proposed refinery and 
					petrochemical complex at Duqm. The way forward will depend 
					on the findings of that study which include a market study 
					as well.<br>
					<b><br>
					What is going to be the role of Oman Oil Company in the 
					development of Duqm?</b><br>
					OOC is mostly in the energy sector through stakes in 
					companies that are engaged in downstream projects, upstream 
					projects, power projects or simply storage tank companies.<br>
					<br>
					I personally believe that Duqm would have many opportunities 
					for OOC as well as to others and OOC is seriously looking at 
					opportunities there. Duqm will be a key port not only for 
					Oman but also for the region. With a gradual transformation 
					into an integrated city with all necessary infrastructure 
					like an airport, I am bullish on Duqm. <br>
					<b><br>
					Recently, there were a few MOU signed with different 
					countries. Could you kindly elaborate? </b><br>
					We have had MOUs with several neighbouring and friendly 
					countries in other parts of the world and we continue to 
					pursue already identified and/or new business opportunities 
					in those countries.<br>
					<b><br>
					A lot of cooperation has taken place between India and Oman. 
					Could you elaborate?</b><br>
					We have excellent relationship with the Republic of India 
					and that has resulted in identification and successful 
					implementation of major joint venture projects like Oman 
					India Fertiliser in Oman, Bharat Oman Refineries in India 
					etc. We are also pursuing several investment opportunities 
					with our counterparts in India. </font></p>
