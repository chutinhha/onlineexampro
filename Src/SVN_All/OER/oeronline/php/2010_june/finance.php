                <p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"> 
                    <br>
                    </font><b><font face="Verdana" size="4" color="#C00000"> 
                    Bullish on GCC markets</font></b><font face="Verdana" size="1"><br>
                    </font><b><font face="Verdana" size="2"><br>
				</font><font face="Verdana" size="4" color="#C00000">
				<img border="0" src="images/jun_finc_img1.jpg" hspace="9" vspace="5" style="border: 2px solid #C0C0C0; " width="100" height="131" align="right"></font><font face="Verdana" size="2">Bank Sarasin is a leading Swiss private bank, acting as an 
				investment advisor and asset manager for private and 
				institutional clients. Dr Burkhard P Varnholt, Chief Investment 
				Officer talks to Sunil Fernandes on equity markets, commodity 
				prices and more</font><font face="Verdana" size="1"><br>
				</font>
				</b><font face="Verdana" size="1"><br>
				<b>Cerebrate Equity markets have rallied sharply over the last 
				few months. Are valuations now lookin</b></font><font face="Verdana" size="1"><b>g stretched?<br>
				</b>My view is that the equity market recovery is lasting longer 
				than many would have thought. We have begun to take away money 
				from emerging markets like Brazil, China and India, as they have 
				turned expensive. Our decision has been prompted by very high 
				valuation in these markets and some monetary tightening that is 
				already happening in countries like China. Within emerging 
				markets our favourite destination remains the Gulf region.<br>
				<br>
				<b>What are the reasons for being bullish on the Gulf markets?<br>
				</b>There are two reasons for this. Firstly, they lagged global 
				emerging markets in 2009. Secondly, our forecast for hydrocarbon 
				prices remain very upbeat and hydrocarbons are the bulwark of 
				these economies. We also believe that financial parameters like 
				price to book values and price to earnings growth make these 
				markets an attractive buy. In fact, we had less exposure to the 
				Gulf region, but are now betting big <br>
				on the region.<br>
				<br>
				<b>Would liquidity in these markets remain an issue?<br>
				</b>The GCC markets are best suited for patient investors. A 
				patient investor would not depend much on liquidity like a 
				speculator would. If you have the patience and the time horizon, 
				things would move in your favour. What I can see in the region 
				is a deeper and broader economic integration. As that happens, 
				stock markets would be increasingly deregulated and hence we 
				will see more investment from global investors.<br>
				<br>
				<b>You talked of price to earnings multiple being high in 
				Brazil, China and India? Do you have any specific price to 
				earnings multiple in mind when you target investments?<br>
				</b>What I am really looking at is price to earnings growth. If 
				a company has a 20 per cent earnings growth, I am happy to pay a 
				decent price for it. However, I am not merely buying because 
				it’s cheap – the point is that there is no magic number. If you 
				see price to earnings and price to book, the GCC companies look 
				far more attractive.<br>
				<br>
				<b>With global commodity prices rising, there are fears of 
				inflation and with it a threat of rising interest rates. What 
				are you views on the scenario?<br>
				</b>My view is very clear that interest rates are going to be 
				lower for longer. And, of course, zero is the bottom. Dollar 
				interest rates will be lower for longer and that would be true 
				for at least another three years. I have no desire to hold US 
				treasury bonds, when they yield <br>
				next to nothing.<br>
				<br>
				<b>But, there have been monetary tightening in certain countries 
				like China and India, while rate hikes in countries like 
				Australia?<br>
				</b>These countries have the benefits of a flexible currency. So 
				they have a choice whether they would like to devalue their 
				currency, whether they want to raise interest rates, or use 
				other monetary mechanisms. Commodity prices are rallying and 
				would continue to do so. Hence, in countries with relatively low 
				income per head, commodity inflation matters most. I tend to 
				think for the global investment community, it is not commodity 
				inflation that matters most, but it’s asset price inflation that 
				will be a surprise. I believe that it will continue to remain 
				so, for the next three years.<br>
				<br>
				<b>How do you see stock markets in the developed region faring?<br>
				</b>One thing that is important to recognise with globalisation 
				and technological development, is that companies have decoupled 
				more strongly from their economies. My forecast for the global 
				economy is a mediocre one, as they will be burdening themselves 
				with higher taxation and slower growth. They may see a much 
				better stock market in the next three years, but the lead will 
				continue to come from emerging markets.<br>
				<br>
				<b>Do you foresee the possibility of a double dip recession 
				happening in the developed economies?<br>
				</b>Clearly, we are of the view that the double dip scenario is 
				not our scenario. We believe that there will be a softening of 
				global indicators and this is very natural. Just as we had a 
				horrific drop in trade and growth, we have had an amazing 
				recovery and now a softening. However, I don’t think that we are 
				going to have negative growth rates all over again.<br>
				<br>
				<b>You said that your own focus would now be changing from 
				emerging markets to the GCC? Do you believe that fund managers 
				are becoming more confident on the GCC now and a trend is likely 
				to emerge?<br>
				</b>I think it will still take a while and I believe one of the 
				reasons for this is that the markets here have not made it into 
				some of the internationally relevant benchmark indices.<br>
				<br>
				<b>Do you believe that oil prices would rise from the current 
				levels of around $85 per barrel?<br>
				</b>I have a high conviction that food, power and water, will be 
				hitting a real bottleneck. Resource availability in these 
				categories becomes limiting in the sense that there would be 
				simply no more of these in future. One number that in my opinion 
				is consistently underestimated and not factored into oil prices 
				is depletion. At some point, supply would not be going up, no 
				matter how deep you dig in offshore Brazil or America. At that 
				point prices will sky-rocket not only for oil and gas, but also 
				for food.<br>
				<br>
				<b>However, we recently had the energy Minister of one of the 
				GCC countries saying that supplies are more than adequate?<br>
				</b>We have always heard that argument, but oil prices continue 
				to go up. You have heard that countries here have invested 
				billions of dollars in the past. In one particular case, a large 
				producing country has invested mammoth sums, but despite that we 
				have not seen a substantial increase in output from that 
				particular country. I believe that depletion may be one of the 
				reasons.<br>
				<br>
				<b>On the supply side, one would see a lot of oil coming from 
				Iraq, which could see downward pressure on prices?<br>
				</b>This supply should happen in three years time at the very 
				earliest and my forecast is what happens between now and the 
				next three years. Do not forget that oil consumption will also 
				increase tremendously, by the time the Iraq oil output hits the 
				market. Of course, Iraq has good reserves and the oil is also 
				easy to extract. I believe Iraq is the best untapped potential 
				as far as oil is concerned.<br>
				<br>
				<b>Where do you see oil prices by the end of the year?<br>
				</b>I believe oil prices would be $100 by the end of the year.<br>
				<br>
				<b>At $100 the alternative energy picture also becomes 
				attractive. Will that affect oil prices?<br>
				</b>Most certainly. The best argument for oil prices going down 
				once again could also be that argument.<br>
				<br>
				<b>Do you believe that the present levels of oil are based more 
				on fundamentals and less on speculation?<br>
				</b>Speculation is much lower than they were during the 
				pre-crisis era.
