                    </font><b><font size="4" face="Verdana" color="#C00000">All 
				eyes on first OMAN green awards</font><font size="2" face="Verdana"><br>
				</font>
				</b><font face="Verdana" size="1">
				<b><br>
				</font><font face="Verdana" size="2">
				OGA will honour, appreciate and promote voluntary efforts and 
				achievements by individuals and organisations</font><font face="Verdana" size="1"><br>
				</font>
				</b><font face="Verdana" size="1">
				<br>
				<br>
				The first Oman Green Awards (OGA) awards function is being held 
				on World Environment Day to celebrate the achievements of 
				organisations and individuals. OGA will give green groups a 
				platform to create awareness, bring about concerted action 
				through exchange of ideas, and propel others to come forth and 
				participate in this social cause.<br>
				<br>
				As the country’s first Environmental Awards, OGA has been 
				created to awaken the eco-consciousness in every citizen and 
				resident. The ultimate aim is to motivate behavioural change and 
				increase awareness in relation to the protection and 
				preservation of our environment.<br>
				<br>
				A large number of entries have been received, both from 
				companies and individuals, in all the eight categories and the 
				results will be announced at a grand function being held at 
				Shangri-La’s Barr Al Jissah Resort &amp; Spa on June 5.<br>
				<br>
				Speaking about the steady stream of entries, Ms Alpana Roy, 
				executive vice president, United Media Services (UMS), said, “As 
				a media house, UMS has a strength to get the ‘green message’ out 
				and we feel it’s our responsibility to make sure it’s the right 
				message. Therefore, as part of our corporate social 
				responsibility, we are delighted to be driving this national 
				platform to popularise the initiatives of companies and 
				achievements of individuals in Oman.”<br>
				<br>
				Going by the number of entries received from companies and 
				individuals for the OGA, it obviously means they understand the 
				role of businesses, institutions and individuals in 
				environmental protection and preservation through responsible 
				and sustainable consumption of natural resources.<br>
				<br>
				And the adoption of environmentally sound technologies, policies 
				and practices is a must to protect the natural resource that we 
				call our earth. The list of companies and organisations that are 
				championing Oman’s green cause are listed on the OGA website 
				www.oeronline.com/greenawards. Meanwhile, OGA’s Green Oman 
				Partners include Bahwan Engineering Company-Carrier; Voltamp; 
				Oman Oil Marketing Company; V-KOOL Oman; Shangri-La’s Barr Al 
				Jissah Resort &amp; Spa; MB Petroleum; Panasonic Ideas for Life.<br>
				<br>
				Oman Green Awards is being supported by the Ministry of 
				Environment and Climate Affairs, the Ministry of Health 
				(Department of Environmental and Occupational Health), Muscat 
				Municipality and the Environment Society of Oman. <br>
				<br>
				Oman Green Awards esteemed panel of judges include Ali Al Kiyumi, 
				Director General for Nature Conservation from Ministry of 
				Environment and Climate Affairs; Dr. Reginald Victor – Dean of 
				Research, Sultan Qaboos University; Dr. Salim Al Wahaibi – 
				Director of the Environmental and Occupational Health 
				Department, Ministry of Health; Mayank Singh, Group Editor, Oman 
				Economic Review and Environment Society of Oman<br>
				<br>
				The Support Partners are Majan Electricity, Oman Printers and 
				Stationers, Times of Oman, Al Shabiba; Business International 
				Group (BIG); Proscape Al Ansari; and Oil &amp; Gas Review.<br>
				<br>
				Speaking about their association with OGA, R Prabhakaran, 
				general manager, Oman Printers and Stationers, says that as a 
				responsible “corporate entity” and with the inherent belief that 
				the environment needs to be preserved, it is an imperative 
				responsibility of every institution to participate in some way 
				or the other in the first National Environment Awards in the 
				Sultanate. An initiative like OGA will raise environment 
				awareness in the Sultanate as it is a big leap in a mammoth 
				social responsibility and this is sure to spread the message and 
				every individual will feel the need to be a part of this 
				initiative.<br>
				<br>
				According to Ahmed bin Said Khamis Al Mazrouy, general manager 
				for Majan Electricity Company (MJEC), going green is a call for 
				survival of all living beings on earth and his company is part 
				of the earth’s ecological system. Mazrouy says the mission 
				statement of MJEC is to deliver a safe, reliable and economical 
				electrical supply to its customers.<br>
				<br>
				Furthermore, MJEC has the obligation to improve customer 
				awareness in conserving electricity use, and minimise system 
				energy loss, thereby reduce gas emission from fossil fuels, 
				Mazrouy added. Continuing further, Mazrouy says, “Our decision 
				to partner Oman Green Awards stems from the fact that it is the 
				first national environment awards in the Sultanate and we want 
				to show our commitment and contribution towards Oman and the 
				world in saving earth and we strongly believe and think that the 
				initiative like OGA will raise environment awareness in the 
				Sultanate.”<br>
				<br>
				Meanwhile, Hemant Pajankar, general manager of Business 
				International Group (BIG) mentions that by partnering with OGA, 
				his company wants to show that it cares for the protection of 
				Oman’s environment. “We encourage more companies to join in the 
				efforts being made by the UMS group for the ‘green’ cause which 
				is in line with His Majesty The Sultan’s vision for Oman,” 
				Pajankar says.<br>
				<br>
				Landscaping company Proscape Al Ansari is in the forefront of 
				environmental activities with its basic activity being 
				landscaping. Says Paul Thomas, manager Operations, “We are 
				instrumental in nurturing a love for plants, which is the vital 
				instrument in reducing the carbon dioxide in the atmosphere. And 
				since OGA is the first national initiative for raising awareness 
				about the need to preserve the environment for the future 
				generations, we feel proud to be associated with OGA.”<br>
