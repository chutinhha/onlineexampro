				<b>How is your company contributing to the cause of environment?
				</b><br>
				Transformers are needed to transport electricity economically to 
				our homes and workplaces. They are designed to raise or lower 
				(i.e., transform) voltage to meet specific electrical needs. 
				Copper-intensive energy efficient transformers can be a very 
				smart decision for conserving precious energy resources and for 
				reducing industrial operating costs and losses in the 
				distribution networks of electric utilities. This is why, in 
				many parts of the world, government regulators are actively 
				promoting energy efficient transformers and industry is 
				discovering the financial benefits of owning them. <br>
				<br>
				When ‘standard’ transformers are used, a lot of energy is wasted 
				in the form of heat. Energy efficient transformers substantially 
				reduce the amount of this wasted energy. Energy efficient 
				transformers rely on high conductivity copper windings. As a 
				general rule, the electrical energy efficiency rating of a 
				transformer increases as the amount of copper is increased. This 
				means that copper, and more of it, enables transformers to run 
				cooler with higher overload capacities – and therefore to run 
				more efficiently. Also, cooler-running energy efficient 
				transformers are expected to have a longer service life. We’ve 
				partnered with the International Copper Association to help you 
				find more ways to save energy and reduce the carbon footprint in 
				the World. Voltamp is in the process of optimising their designs 
				further and is working with utility companies for improving the 
				technical specifications for more energy efficient transformers.
