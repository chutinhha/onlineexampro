			<br>
			The sprawling Marqués de Riscal hotel nestled along a hillside in 
			Elciego town in northern Spain evokes a range of feelings like 
			exclusivity, flamboyance, exuberance and most notably uniqueness. 
			The hotel, ideally located in wine country Rioja Alavesa and set 
			against the backdrop of a beautiful rural church, with colourful 
			titanium curves swirling to all sides, is really a statement of 
			style created by Frank Owen Gehry, the renowned architect whose 
			buildings have become tourist attractions, simply because of his 
			badge of distinction.<br>
			<br>
			Then, it was only appropriate that the RCZ sports coupé, which marks 
			a new stage in the line up of Peugeot’s “leisure vehicles”, was 
			lined up for a test drive at this very hotel. The Peugeot RCZ 
			gaining reputation as more than just the embodiment of a concept 
			car, they say, was designed to evoke emotion in motion with its 
			expressive and innovative style, exclusive interior, dynamic and 
			environment-friendly engines and the driving enjoyment it produces. 
			All made to project the Peugeot brand, buoyed by 200 years of 
			history, into a future that promises more emotion than ever.<br>
			<br>
			The first stage of the RCZ project began in September 2007, at the 
			Frankfurt Motor Show, when Peugeot unveiled a concept car, which 
			excited all that saw it. After two years, the concept became a 
			reality at a production centre specialising in exclusive vehicles at 
			Magna Steyr plant in Graz, Austria. Peugeot disclosed that buyers 
			were quick off the mark to reserve the first 200 “Limited Edition” 
			RCZ models, which sold out in less than 48 hours at the 2009 
			September’s Frankfurt Motor Show. The RCZ was indeed born under the 
			sign of exclusivity, strong design and emotion.<br>
			<br>
			<b>Exterior emotion</b><br>
			Athletic, elegant and aerodynamic, the style of the RCZ offers a new 
			experience and energy derived from the original architecture. The 
			sensuality of the car’s double bubble roof and rear windscreen, 
			combine with its two aluminium arches to produce its most 
			distinctive features. The feline look of the car is unmistakable and 
			definitely is a head turner.<br>
			<br>
			<b>Interior emotion</b><br>
			With an ideal driving position, a fluid and streamlined fascia 
			panel, traditional and high-tech materials, the passenger 
			compartment of the RCZ instantly immerses the driver in an elite, 
			sporting ambience.<br>
			<br>
			The RCZ is also practical, offering two occasional rear seats whose 
			backrests can be folded to open up a boot with a generous capacity 
			in its category (from 384 to 760 litres). A small drawback for the 
			passenger is the lack of hand-rail which was compromised due to 
			seamless aluminium arches of the car body.<br>
			<br>
			The Peugeot RCZ is exclusive in other ways as well. To begin with, 
			its name, RCZ, symbolises its singular position in relation to the 
			Peugeot range: it is the first specialist car not to use the 
			numbering system with a central or double zero and secondly, the RCZ 
			illustrates the Marque’s “new identity”, and is the first product to 
			benefit from the latest Peugeot Lion brand identity.<br>
			<br>
			The RCZ, a sporty and compact 2+2 coupé, is aimed at the modern 
			customer, particularly demanding and well informed when it comes to 
			cars, who seeks to stand out… and is ready to succumb to automotive 
			temptation.<br>
			<br>
			<b>Engineering emotion</b><br>
			With its reduced weight, finely honed aerodynamics and modern engine 
			technologies, the RCZ offers an innovative blend of performance and 
			environmental friendliness.<br>
