				The Basque country-side of Northern Spain is driving paradise 
				with beautiful roads meandering through undulating fields and 
				through mountainous areas which take you past some of the most 
				stunning locales. The 250km test drive from the Marques de 
				Riscal Hotel began in the afternoon and took us past quaint 
				little villages and sometimes on the highways giving room to 
				test the car’s capabilities on straight roads, tight curves as 
				well as the steep slopes of the mountains.<br>
				<br>
				During the 250km drive we had the option of switching between 
				the 2.0 litre, 163 bhp, diesel and the 1.6 litre Turbo 200 bhp 
				petrol version all equipped with a 6-speed manual gearbox but 
				since diesel engines are not exactly favourites in Oman, we 
				stuck to the 1.6 Turbo petrol version.<br>
				<br>
				<b>Dynamic emotion</b><br>
				Expressing all of Peugeot’s expertise in terms of road holding, 
				the RCZ provides a thrilling experience with precision and 
				driveability, creating a sense of real excitement and occasion 
				for the driver.<br>
				<br>
				The RCZ benefits from a strong base – that of “platform 2” 
				architecture (MacPherson type front suspension, rear suspension 
				with deformable cross member). It also has a particularly low 
				ride height and centre of gravity, with a wider track and 
				generous wheel dimensions of 18” or 19” diameter.<br>
				<br>
				Lastly, the RCZ can be specified with the THP 147 kW (200 bhp) 
				engine. Uniquely to this version, the front suspension 
				incorporates a special lower anti-collision bar to give the car 
				even livelier and more agile handling response, while 
				guaranteeing exceptional stability.<br>
				<br>
				To match its dynamic, exclusive positioning, and in addition to 
				its stylish and mechanical innovations, the RCZ comes equipped 
				with sophisticated features and driving aids to ensure both 
				peace of mind and safety with ESP, hill start assist (as 
				standard), WIP Com 3D (top of the range multimedia system), 
				directional bi-Xenon headlamps, etc.<br>
				<br>
				<b>Emotion à la carte</b><br>
				So that customers can transform their car into a unique 
				extension of their personal preferences, the RCZ can be 
				personalised in line with the car’s exclusive character, with 
				the choices available including: carbon roof, arches from a 
				range of different colours, a wide choice of alloy wheel 
				designs, etc. Peugeot also intends to maintain its leadership in 
				terms of tertiary safety. For example in 2010 the Marque will be 
				deploying, at no extra cost and throughout the RCZ range – in a 
				world first – services including Peugeot Connect SOS (emergency 
				call) due in particular to the adoption of a “localised 
				communication unit” system.
