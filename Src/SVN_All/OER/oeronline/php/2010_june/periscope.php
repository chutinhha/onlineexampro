	<tr>
		        <td valign="top" width="100%"> 
				<p align="justify" style="margin: 3px 10px"> 
					<b><font face="Verdana" size="2">The fall in crude oil, the 
					politics of Washington restrictions on coastal offshore 
					drilling and liabilities related to the Deepwater Horizon 
					oil have devastated the shares of Transocean, the owner of 
					BP’s oil rig in the Gulf of Mexico which blew up<br>
					</font></b></p>
				<p align="justify" style="margin: 3px 10px"> 
					<font face="Verdana" size="1">“Oil services and contract 
					drilling companies are ubiquitous in the world’s oil 
					provinces, including Oman and the GCC states. The fortunes 
					of the oil service/drilling firms depend less on crude oil 
					than on the capital spending budgets of the world’s largest 
					private and state owned companies, colossi such as BP, Exxon 
					Mobil, Shell, Chevron, Lukoil, Saudi Aramco, Pemex and PDO.<br>
					<br>
					However, oil services companies face the same geopolitical 
					or environmental liabilities risk as the super major oil 
					companies whose oilfields they service or for whom they 
					operate their drilling fleets on contract. This was 
					tragically demonstrated when the recent BP oil rig, owned by 
					Transocean, blew up. The financial markets have turned 
					bearish on oil service companies, whose shares fell 20 – 25 
					per cent in a month after the accident. Their fall was 
					accelerated when oil prices fell by $15 on Greek debt woes 
					and the surge in the dollar against the Euro. However, panic 
					on Wall Street has created an opportunity in oil services 
					firm Weatherford and contract driller Transocean, two 
					classic fallen angels.<br>
					<br>
					<b>Iraq factor<br>
					</b>The reconstruction of Iraq’s oil fields could well prove 
					to be the most compelling macro theme in energy investing 
					for the next decade. Iraq floats on an ocean of black gold, 
					with the world’s third largest proven reserves and an oil 
					infrastructure devastated by Saddam’s wars against Iran and 
					Kuwait, UN sanctions, civil war, Desert Storm and the 2003 
					American invasion. Yet Iraq’s dismal oil production 
					prospects (current production of 2.4 million barrels is way 
					below 1980’s OPEC quotas) have changed dramatically in 2009. 
					A new Petroleum Law has been accepted by the Baghdad 
					government as well as the autonomous Kurdish regional 
					authorities. The recent oil exploration concessions have 
					been awarded to ten of the world’s leading oil and gas 
					colossi.<br>
					<br>
					In essence, the Seven Sisters have won 20 year contracts to 
					pump Iraqi crude for the first time since Saddam 
					nationalised the oil industry in the 1970’s. Iraq, in my 
					opinion is the world’s greatest growth opportunity for Big 
					Oil and the oil service firms, with six million barrels per 
					day (MBD) in exports entirely possible in the next six 
					years. Not even deepwater Brazil can match the sheer scale 
					of Iraq’s estimated 110bn reserves.<br>
					Iraq is the major reason I am bullish on the prospects of 
					Weatherford (WFT), which has won oil services/drilling 
					contracts in both BP’s Rumaila field as well as in Iraqi 
					Kurdistan. While Schlumberger is obviously the world’s 
					biggest, classiest oil service firm, it trades at a premium 
					valuation of 20 times earnings and Iraq is a miniscule 
					fraction of its global earnings.<br>
					<br>
					WFT is a far more compelling, focus play on the Iraqi oil 
					services spending market, which could be worth $40–$50bn in 
					five years. This figure could rise if Iraq oil reserves 
					exceed 200 billion barrels, as prominent geologists and 
					energy economists expect. WFT, far smaller than 
					Schlumberger, Halliburton and Baker Hughes, will benefit 
					most from the Iraqi oil bonanza. The market does not 
					remotely rate Weatherford as a growth stock with a 
					price/earnings growth ratio of only 0.75, half the valuation 
					of Schlumberger. Nor am I entirely convinced that WFT can 
					manage the huge capex required to exploit its Iraqi 
					footprint. This means that Weatherford is a compelling 
					takeover candidate for Halliburton or Baker Hughes, not that 
					Schlumberger has bid and won Smith International (40 per 
					cent premium) and France’s Geophysique WFT’s international 
					portfolio in Russia, the Gulf, Iraq, West Africa and 
					offshore Brazil should surely be attractive to Halliburton, 
					which desperately needs to boost its <br>
					non US business.<br>
					<br>
					Wall Street oil service analysts are not gushingly bullish 
					about WFT. Fine. This means earnings upgrade and a potential 
					takeover premium is not priced in the shares at $15. I can 
					easily see WFT trade at $25 in the next year even if no 
					takeover bid emerges for the NYSE best Iraqi oil services 
					play, the Cinderella among contract drillers.<br>
					<br>
					<b>Collateral damage<br>
					</b>Transocean (RIG) owns the world’s largest oil and gas 
					drilling fleet. It was the operator of the Deep Water 
					Horizon offshore rig on behalf of BP that exploded, cost the 
					lives of thirteen oil wells and created the worst oil spill 
					in American history since the Exxon Valdez supertanker ran 
					aground in Alaska two decades ago. This tragic accident has 
					gutted Transocean shares, which fell from $94 in April to 
					the $65–68 range now. The fall in crude oil, the politics of 
					Washington restrictions on coastal offshore drilling and 
					liabilities related to the (insured) Deepwater Horizon oil 
					have devastated the shares of Transocean. Transocean shares 
					could fall as low as $54–$58 because headline risk from 
					Washington and the Gulf of Mexico has created a perfect 
					storm for the company.<br>
					<br>
					However, as always, I believe the financial markets have 
					overreacted to the Deepwater Horizon disaster. BP is liable 
					for clean up costs, environmental costs and damages to the 
					Louisiana shrimp fishermen, whose livelihoods are hit by the 
					oil spill. RIG’s insurance is more than a one billion 
					dollars, which should cover personal injury, the removal of 
					the doomed rig, third party claims and legal costs.<br>
					<br>
					The world’s largest offshore drilling rig fleet now trades 
					at a rock bottom valuation of six times earnings and four 
					times forward enterprise value/EBITDA. This company could 
					well deliver $8 in 2010 and $11–$12 in 2011. No other oil 
					service company in the world can match the size of RIG’s 
					deepwater drilling fleet and deepwater (even more than LNG) 
					is the future of the global energy business, as the euphoria 
					over Brazil and West Africa proves. The tragic accident will 
					not end the contract drilling business any more than the 
					Apollo 13/Challenger disasters killed the American space 
					programme. This means Tranocean (RIG) is a classic fallen 
					angel in the oil service business. An entry point in the 
					$54–58 range for a $100 target is optimal. </font></p>
