		        <td valign="top" width="100%"> 
				<p align="center" style="margin: 3px 10px"> 
					<b><font face="Verdana" size="4" color="#C00000">
				<img border="0" src="images/DSC_7193.jpg" hspace="9" vspace="2" style="border: 4px solid #C0C0C0; " width="65" height="89" align="left"></font></b></p>
				<p align="justify" style="margin: 3px 10px"> 
					<font face="Verdana" size="2"><b>Dr K C Chakrabarty, Deputy 
					Governor, Reserve Bank of India makes a compelling case for 
					India as an investment destination in an interview with 
					Mayank Singh <br>
					</b>
					<br>
					&nbsp;</font></p>
                </td>
		</tr>
	<tr>
		        <td valign="top" width="100%"> 
					<p align="justify" style="margin: 3px 10px"><b>
					<font face="Verdana" size="1">What makes India an attractive 
					investment destination for foreign capital?<br>
					</font></b><font face="Verdana" size="1">India has a number 
					of factors working in its favour -- a destination is 
					attractive for investment when the risk adjusted returns are 
					good, India has never defaulted on its sovereign debt and 
					has given good returns. It is growing at seven to eight per 
					cent. It is a trillion dollar economy, has a democratic 
					system of governance and a strong legal framework. An 
					English speaking skilled manpower base and a young 
					population adds to its appeal. <br>
					<br>
					<b>Are you visiting Oman for a specific purpose or is it a 
					broad based agenda?<br>
					</b>I am not here on a specific mission. My agenda is to 
					meet people. This is the 75th year of the Reserve Bank of 
					India and we are reaching out to our stakeholders and 
					customers about how they feel about us. <br>
					<br>
					<b>How important is the GCC region for India as a trading 
					partner?<br>
					</b>The GCC as a whole is very important. We have a trade 
					deficit and a large part of it is compensated by remittances 
					and invisibles. As we have a large population of the 
					non-resident Indian’s (NRIs) living and working in the GCC 
					area, they are an important component of our relations with 
					the region. It is also one of the closest regions from India 
					geographically. Our proximity and cultural relations date 
					back to 300-400 years. India is a developing country that 
					needs energy and the GCC region is the main supplier of 
					energy to the entire globe. So to that extent too it is 
					important. <br>
					<br>
					<b>Is the policy regime in India conducive and welcoming for 
					NRI’s to invest in India and for Indian’s to invest abroad?
					<br>
					</b>We give NRI’s the best terms, actually it is better even 
					than the rules for resident Indians. This is the time when 
					Indians should go out and do business across the globe and 
					for that we need to create a network and distribution 
					channel. We need to acquire new companies and this cannot be 
					done unless Indians are allowed to invest abroad. The 
					country does not have a problem of foreign exchange and we 
					want to utilise foreign inflows in the best possible manner. 
					Unless we allow Indians to acquire assets abroad it is 
					difficult to manage such capital flows. <br>
					<br>
					<b>Internationally, Central Banks have kept interest rates 
					at low levels, what is the RBI’s stand on interest rates?<br>
					</b>Those countries which are a borrowing economy keep 
					interest rates low as the savings rate in these countries 
					are low. Secondly, if one has the ability to borrow at low 
					interest rates then one can keep interest rates low. It 
					suits them but it does not suit a country like India where 
					investments are linked to savings and we have to encourage 
					people to save. So it is important that savers are 
					compensated against inflation and are given a minimum return 
					on their money. Low global interest rates also creates a 
					problem of volatility of capital flows. Our objective is to 
					keep interest rates benign and to ensure that savers get a 
					reasonable return and to keep the investment flows 
					continuously growing to avoid unnecessary capital inflows. 
					Very low interest rates across the globe makes our life 
					difficult. <br>
					<br>
					<b>The corporate sector would like to have low interest 
					rates, how do you balance your other compulsions with the 
					needs of business houses?<br>
					</b>I don’t think that the corporate sector only wants low 
					interest rates, people actually want to avoid volatility of 
					interest rates. If companies know that interest rates are 
					going to be at 15 per cent, they will accordingly price 
					their products. The corporate sector can get cheaper money 
					if banks are ready to give money at low rates, and banks are 
					in turn dependent on peoples savings. So companies are not 
					always looking at low rates but competitive rates so that 
					they can compete across the globe. Second, they want the 
					volatility of interest rates to be kept under control. <br>
					<br>
					<b>How big is inflation a problem for the Reserve Bank of 
					India?<br>
					</b>Inflation is a big problem at present. We have two sets 
					of inflation, one is the wholesale price and other is the 
					consumer price and there is a divergence between the two. We 
					have been able to keep inflation low for the last four to 
					five years and it has come down to 3-4 per cent levels. But 
					all of a sudden food prices have gone up, partly because of 
					global commodity prices and higher food prices which have 
					gone up partly because of our policy of compensating farmers 
					with better procurement prices and both these factors have 
					added to inflation. Apart from these we have supply side 
					factors, like the need to produce more sugar, pulses, 
					vegetables etc. All these factors have taken inflation to 
					18-19 per cent, but non food and non manufacturing inflation 
					is manageable. We know that if food inflation continues it 
					will spill over to other areas and at some stage the supply 
					factors will also start impacting demand factors and 
					therefore we need to be cautious. Our entire policy 
					mechanism is geared towards managing the recovery process we 
					believe that growth has already started. Growth is strong 
					but inflation has to be kept down. <br>
					<br>
					<b>Are there any policy measures that the RBI is taking to 
					curb inflation?<br>
					</b>Since its supply side issues that are giving a fillip to 
					inflation we are keeping an eye on the situation and are 
					trying to keep a check on liquidity in the system. We have 
					to ensure that demand side factors do not superimpose 
					themselves on supply side factors and make matters worse.
					<br>
					<br>
					<b>If you reduce liquidity will it impact growth?<br>
					</b>No we do not think so. <br>
					<br>
					<b>Can we expect to see full capital account convertibility 
					soon? <br>
					</b>We have to manage the capital account for a considerable 
					time. Though on the current account the rupee is fully 
					convertible. For foreign investors it is already fully 
					convertible, for NRI’s it is almost convertible, even for 
					resident Indians we have liberalised rules, but totally 
					liberalising capital flows is not desirable as our fiscal 
					situation, budgetary deficit, and inflation situation does 
					not allow us to do so and for a considerable time we need to 
					manage capital flows in the country. <br>
					<br>
					<b>The world went through this economic crisis. How far has 
					India been impacted by it? <br>
					</b>In today’s globalised environment you cannot say that 
					one has not been affected by such a crisis. We have been 
					impacted but there are two things that are clear – India’s 
					contribution in creating this global crisis is nil. Second, 
					when the recovery process starts we will be among the few 
					countries which will come out of it fast. We have not 
					experienced an internal banking or financial crisis. Since 
					world’s growth rate has slowed down, we have been affected 
					by the erosion of confidence and the slowing of trade. We 
					have a strong domestic demand and are trying to create 
					growth by generating domestic consumption. So overall, we 
					have not been so vulnerable. In 2010 our growth rate is 
					expected to be 7.5 per cent and we expect 2010-11 to be 
					better. </font>
