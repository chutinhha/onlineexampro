                <td height="605" valign="top"> <b> 
			<p align="justify" style="margin-left: 10px; margin-right: 10px" dir="ltr"><font face="Verdana" size="1"> 
                <br>
                <br>
                </font><font size="1" face="Verdana, Arial, Helvetica, sans-serif"> 
                    <img src="images/automobile01.jpg" hspace="9" vspace="5" border="0" align="right" width="147" height="164" style="border: 3px solid #EFEFE9; "></font><font size="4" face="Verdana" color="#CC3333">The 
			hero comes home</font><font size="1" face="Verdana, Arial, Helvetica, sans-serif"><br>
			<br>
                </font><font face="Verdana" size="2">Nissan launched the 2010 
			edition of The Patrol recently in the Middle East. Hiroyuki Fushiki, 
			Segment Chief Product Specialist, shares the unique features of the 
			SUV with OER<br>
			</font><font size="1" face="Verdana, Arial, Helvetica, sans-serif"><br>
                <br>
                Nissan Patrol is a name that is synonymous with ruggedness; a 
			legendary off-roader capable of going anywhere, anytime. 
			Meticulously honed for over five decades, the Patrol is Nissan’s 
			flagship SUV model and enjoys a rich heritage and passionate 
			following in the Middle East that dates back to the 1950’s. It is no 
			small statement to say that for many owners across the Middle East, 
			the Patrol is ‘The Hero Come Home’. <br>
                </font></b> <font size="1" face="Verdana, Arial, Helvetica, sans-serif">
			<br>
			The Patrol now returns as an all-new seventh generation model, 
			building on its impressive history, but at the same time, 
			incorporating a new platform, a radical new design, significantly 
			enhanced interior comfort and luxury, a powerful new V8 engine, a 
			seven-speed automatic transmission and a new 4WD system. <br>
			<br>
			“The new Patrol is built with a high level of safety, performance 
			and quality that characterises the Nissan brand, and we paid special 
			attention to the needs of GCC drivers and passengers,” said Carlos 
			Ghosn, President &amp; CEO of Nissan Motor Company at the launch. “We 
			are confident that our flagship SUV for the Middle East will exceed 
			all expectations,” he added.<b><br>
			</b><br>
			Wanting to build the best possible Patrol, engineers first tuned and 
			tested 200 prototypes for dynamic evaluation at the company’s 
			Motegi, Oppama and Tochigi proving grounds in Japan before sending 
			them to the Middle East where they were exposed to a range of 
			challenges -- desert, rocky surfaces, highways and inner-city 
			traffic. In fact, more than 40 Japanese engineers completed a total 
			of 13,265 hours of testing in the region to assess the vehicle’s 
			performance and durability. A rigorous programme of tests was 
			carried out by Nissan engineers between early 2008 and the end of 
			2009, in the tough terrain areas close to the UAE and Saudi border.
			<br>
			<br>
			Says Hiroyuki Fushiki, segment chief product specialist, product 
			planning department, product strategy and product strategy and 
			product planning division, Nissan, “The Patrol is The King of the 
			Desert and we wanted the new vehicle to be a more powerful, 
			comfortable and luxurious vehicle than its previous versions.” After 
			extensive market research, especially in the Middle East, the design 
			team concluded that for the Patrol to beat its main competitors, it 
			needed a brand new design direction, class-leading engine 
			performance, greater luxury and comfort and a smart mix of new 
			technologies that enhanced the total driving experience. <br>
			<br>
			“The all-new exterior design is like a breath of fresh air. The new 
			surfaces are authentic to Patrol heritage and at the same time 
			beautifully add a sense of modernity to the design equation, while 
			still maintaining a ruggedness and commanding road presence,” says 
			Fushiki. <br>
			<br>
			One design element that expresses ‘Patrol’ and had to feature 
			strongly was the angled-strut motif front grille. It was 
			meticulously proportioned to deliver a sense of continuity with past 
			models but also give a touch of newness while enhancing the car’s 
			status level. This off-road trendsetter was one of the first 
			dedicated four-wheel-drive vehicles available in the climatically 
			demanding Middle East region, and boasts the notable distinction of 
			also being the first vehicle to successfully cross the grueling 
			Simpson Desert in Australia back in 1962.<br>
			<br>
			<b>Luxury personified <br>
			</b>Featuring generous amounts of leather, polished aluminium and 
			wood grain paneling, the Patrol’s interior too has been 
			significantly upgraded. Says Fushiki, “Designers wanted to create a 
			better, higher quality interior for the GCC family. In keeping with 
			the Patrol’s mission to deliver more comfort and more room, the new 
			Patrol delivers seat dimensions that outclass all competition.” 
			Boasting wider, higher and deeper front seats, the Patrol offers 
			over 100mm more legroom for second row passengers, and a roomier 
			third row of seats as well. Employing double the lateral rigidity 
			over the outgoing model, the new Patrol sits on a revised platform 
			that now offers a more robust construction with a low vibration 
			body.<br>
			<br>
			The new Patrol is the most powerful vehicle in its class. The key 
			technology achieving this jump in performance over the predecessor’s 
			4.8 litre power plant is the newly developed ‘VK56VD’ 5.6-litre V8 
			engine with VVEL (variable valve event and lift) and DIG (direct 
			injection gasoline) technologies that generate 400hp and 560Nm of 
			torque with enhanced fuel efficiency and cleaner emissions on the 
			top specification version of this new engine. Says Fushiki, “Every 
			component has been redesigned for maximum performance and smooth 
			response, while a new 7-speed automatic transmission with manual 
			shift mode and a wider range of gear ratios than its predecessor, 
			gives the off-roader the ability to deliver power faster.” <br>
			<br>
			In a world-first, the new Patrol employs a Hydraulic Body Motion 
			Control System (HBMC), which keeps the car flatter with less body 
			roll during lane-changing and cornering, while delivering a 
			comfortable ride in city driving and superb stability on rugged and 
			sandy tracks. Another crucial part of the handling equation comes 
			from the new suspension system, which now employs 4-wheel 
			independent suspension, upgraded from the previous rigid axle setup. 
			Nissan has completely redesigned the vehicle’s suspension from the 
			bushes to the links. The combination of HBMC and the new suspension 
			system with its longer suspension stroke delivers significantly 
			improved handling and stability on both on-and off-road terrain.<br>
			<br>
			The Patrol also employs a newly developed All mode 4x4 system 
			incorporating a variable 4x4 mode package. It allows the driver to 
			quickly switch between four drive modes: sand, on-road, snow and 
			rock, with the flick of a switch. In sand mode, owners are able to 
			drive safely, quickly and skillfully in sand and dirt, just like 
			professional rally drivers. In addition, the Patrol also offers a 
			new Hill Start Assist and Hill Descent Control. <br>
			<br>
			<b>Safety comes first <br>
			</b>Nissan Patrol comes with a number of safety features. It has 
			employed state-of-the-art safety technologies that help the driver 
			maintain comfortable driving, to recover from dangerous conditions 
			and minimise the damage when a collision is unavoidable. 
			Incorporated in the Nissan world-renowned Safety Shield, the safety 
			systems include lane departure warning and prevention (LDW), which 
			assists the driver avoid unintended lane departure. If the Patrol is 
			getting close to the lane markers unintentionally, the LDW warns the 
			driver with an alarm and an indicator to brake. At the same time, 
			LDW assists the driver to bring the vehicle back to the lane by 
			generating a turning force by applying the brake.<br>
&nbsp;<br>
			Says Fushiki, “The GCC will make up a big portion of the new 
			Patrol’s market. We took into account the severe road market 
			conditions in the GCC and after reading the market our engineers 
			have made a vehicle that suits these conditions.” The Patrol sees 
			the Toyota Landcruiser as its main competition. “The SUV market has 
			changed over the years. Earlier there used to be Toyota and Nissan 
			and later Mitsubishi entered the 4x4 market. But now even US and 
			European manufacturers have started making good SUV’s. We compete 
			with all of them but Toyota Land Cruiser is the most similar in 
			characteristics to the Nissan Patrol and most customers in the GCC 
			recognise the Land Cruiser.”<br>
