				<font face="Verdana" size="4" color="#C00000">
				Growth is a way of life</font></b><font face="Verdana" size="1"><br>
				</font><b><font face="Verdana" color="#333333" size="2"><br>
				Hani Dawood al Zadjali has worked tirelessly on adding to his 
				knowledge base through education and experience enabling him to 
				do well</font></b><font face="Verdana" size="1"><br>
				<br>
				To advance in your career you need to grab every opportunity and 
				even half opportunities that come your way. I was there when 
				opportunities came my way,” says Hani Dawood Al Zadjali, head, 
				Human Resource Development, Oman Refineries and Petrochemicals 
				Company (ORPC). An alumni of Saidiya School and Ruwi Secondary 
				School he went to Kuwait to study Business Administration. 
				Though the course usually takes five years to complete, he 
				completed the course in three and a half years, by taking 
				additional classes during summers. On coming back to Oman he 
				joined the State General Reserve Fund (SGRF) as a systems 
				analyst in its IT department. It was here that he cut his teeth 
				in human resource management. Never one to sit on his laurels he 
				went onto get professional certifications from Oracle and 
				Microsoft. <br>
				<br>
				In 2006, he joined Sohar Refinery as an HR development officer 
				and did a course from the Chartered Institute of Personnel and 
				Development (CIPD). The decision to shift from SGRF was not an 
				easy one as he was offered a RO8,000 scholarship for doing a 
				masters at SGRF. When he decided to shift to ORPC, the fund 
				threatened to withdraw the offer if he changed jobs, after some 
				deliberation Hani stuck to his decision to join ORPC at the cost 
				of the scholarship. Looking back it proved to be the right move 
				as after the merger of Sohar Refinery and ORPC he was given the 
				charge of the entire HR department in the merged entity.<br>
				<b><br>
				The edge</b><br>
				“I am a fast learner, when I joined SGRF, IT was a new field for 
				me but over two years I built up my expertise in the field and 
				became one of the pillars of the company. Even at ORPC, there 
				were people who had 10 to 20 years of experience, but I got the 
				chance to head the HR department. This proves the point that if 
				you work hard you will achieve things,” says Hani. And when it 
				comes to work he brooks no compromises, like for example he gave 
				his CIPD dissertation on the day that he got married. “I had a 
				two week deadline to submit my dissertation on ERP, I kept on 
				communicating with my supervisor in UK and on my wedding day 
				went to the printing office and couriered my dissertation and 
				then went for my wedding. I was constantly juggling the two 
				responsibilities.” <br>
				<br>
				He feels that the window of opportunity is always there for 
				youngsters, but it all boils down to who is willing to grab 
				those chances. “Developing ones career is a challenge, but it 
				can be done if one has the right skills and temperament. It is 
				also important to coach youngsters so that they can do well.” 
				ORPC has a mentoring strategy to develop skills of young Omanis. 
				“The difference between a successful person and an unsuccessful 
				person is that the former takes the initiative to ask for work, 
				while the latter waits for things to come to them,” says Hani. 
				As the competition hots up it is important for people to stand 
				out. When Hani applied for a vacancy at ORPC he was surprised to 
				see 82 aspirants for a single job.<br>
				<br>
				He lists out a number of skills that young Omanis should try and 
				acquire – on the personal side one needs to have management, 
				analytical thinking, planning, teamwork and presentation skills. 
				While on the technical front one can specialise in various 
				disciplines like IT, HR, CIPD, CAT, ACCA etc. The right 
				behaviour and attitude are also of paramount importance as Hani 
				says, “One’s technical skills can be developed but attitude is 
				important because it reflects how a person will react to a 
				certain situation. Your attitude reflects your values, beliefs 
				and complements one’s work.”<br>
				<b><br>
				Big canvas</b><br>
				He feels that in the last 40 years we have achieved a lot but we 
				cannot just depend or rest on our past laurels. We need to say 
				that this is not enough, this is just the beginning. We have 
				talented youngsters and need to believe that we can do much 
				better, says Hani. He feels that there are times when people 
				start linking their personal matters with ones organisation and 
				it is important that people differentiate between what is 
				personal and what is professional. Young people need to take 
				constructive feedback positively so that they can grow. <br>
				<br>
				An avid traveler, finds it to be a great learning process. His 
				various tours to the US, UK, East Asia, Gulf etc have taught him 
				a lot about those cultures and people. For example, he admires 
				the sense of customer service in the US, discipline and planning 
				in Japan etc, “I was in Japan and they gave us an itinerary 
				stating that we had to leave the hotel at 12.07 pm, arrive at 
				the station at 12.11pm and board a train at 12.13 pm. I 
				marvelled at their discipline,” says Hani. <br>
