				<font face="Verdana" size="4" color="#C00000">A focused approach<br>
				</font></b><font face="Verdana" size="1"><br>
				</font><b><font face="Verdana" color="#333333" size="2">
				Temperament and life skills acquired from sports have helped 
				Taha Husain Al Raisi to excel in his corporate career<br>
				</font></b><font face="Verdana" size="1"><br>
				Taha Husain Al Raisi, department head of radio optimisation, 
				Nawras recollects an anecdote while working as a radio planner 
				with Omantel. “Our task was to connect two hubs in the Quriyat 
				area and as we could not hire a helicopter we decided to go 
				ahead on foot. The process would have been delayed by three 
				months if we failed to finalise on the sites that day. Starting 
				out we realised that we had to climb four mountains one after 
				the other in the Al Amerat area from 3pm onwards during summers. 
				After the team had climbed two mountains we found that it was 
				very hot and humid, to make matter worse we ran out of water.”<br>
				<br>
				Led by Taha the team prodded on and completed the work at hand. 
				By the time they came down a few of his colleagues had to given 
				medical help. Though it was difficult it proved to be a good 
				learning experience. This never say die attitude has helped Taha 
				to do well in a short career span. Coming from what he describes 
				as a telecom family (as his father, mother, uncle and cousins 
				worked in Omantel), joining the sector was an obvious choice for 
				him. Having started as a project engineer in Omantel over a four 
				year period he got promoted as a radio planner, a 3G team leader 
				and an acting section head.<br>
				<br>
				When Nawras was looking for a 3G planner and project manager he 
				saw it as an opportunity to take on a new challenge. As he had 
				been involved in selecting a 3G vendor and the requisite 
				equipments at Omantel, Taha had built up a good knowledge base. 
				In 2006 he joined Nawras as a 3G project manager. Two years 
				later he got promoted as a 3G planning and optimisation section 
				head and in 2009 he was promoted as a department head of 
				optimisation for 2G and 3G. “Nawras has a multicultural 
				environment and people are judged by their performance. Age and 
				nationality is not an issue and as long as you perform you can 
				grow in this company,” says Taha.<br>
				<br>
				<b>Looking back<br>
				</b>He did his primary and secondary education at Saidiya school 
				in Muscat, the oldest school in Oman and Ruwi Secondary school. 
				At the age of ten he started playing handball and has been a 
				keen sportsperson since then. He recollects the toil involved in 
				pursuing his passion, “As I stayed in Muscat it used to take me 
				an hour to walk down to Sidab club and then we played two to 
				three hours in the sun. I used to walk slowly to the club to 
				conserve energy and on match days, I walked through a mountain 
				which was a shorter distance.” All that hardwork though has paid 
				off in hindsight.<br>
				<br>
				Taha has been the captain of Oman’s handball team for the last 
				five years. The team finished runners up in the GCC cup in 2009. 
				At the club level he has won 11 league titles – nine with Sidab 
				and two while playing for Seeb. He feels that sports teaches one 
				to be a team player, inculcates discipline, hard work, ambition 
				and motivation. “You learn how to coach others, so that they 
				become better and help the team. In handball we use a lot of 
				body language and by reading people’s faces one can understand 
				that there is a question or a query. Overall it has helped me to 
				become a leader,” says Taha.<br>
				<br>
				At one stage he realised that he needed to curb his passion to 
				concentrate on his studies. “I was unfocussed and as luck would 
				have it broke my hand before my 12th exams – which was a crucial 
				factor in deciding my future,” says Taha. He felt that going 
				abroad for higher studies would help him to do better and he 
				decided to do a bachelor’s in electronic science from Chennai in 
				India. The exposure helped his confidence. At college he played 
				in the college basketball, handball and volleyball teams apart 
				from cricket and Kabaddi for fun. He was a foreign students 
				representative and deputy in-charge of the mess. “If you respect 
				people, they respect you. I have never shied away from seeking 
				help or getting help, sharing knowledge or approaching people,” 
				says Taha. On coming back to Oman he got job offers from PDO and 
				Omantel and he took up the latter and as they say there has been 
				no looking back since then. <br>
				<br>
				Looking at young Omanis he feels that they are talented but need 
				coaching and proper training. “Omanis are humble and nice but 
				they are also smart and hardworking. On the flip side they get 
				affected by the environment, if the place is competitive they 
				become competitive, but if there are lazy people around they 
				tend to become like them. This may not be true for everyone, but 
				holds true for a majority of youngsters,” says Taha.<br>
