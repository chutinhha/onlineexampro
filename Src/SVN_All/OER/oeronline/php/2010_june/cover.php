				<font face="Verdana" size="4" color="#C00000">Multifaceted 
				talent<br>
				</font></b><font face="Verdana" size="1"><br>
				</font><b><font face="Verdana" color="#333333" size="2">A bundle 
				of talent Madiha al Bakri is a firm believer in providing more 
				opportunities for youngsters to flourish in Oman<br>
				</font></b><font face="Verdana" size="1"><br>
				An artist, a photographer, an MC, a DJ, a swimming trainer, 
				musician and a promising executive, Madiha Al Bakri, assistant 
				manager, Basma Marketing and Business Development, Oman Oil 
				Marketing Company (OOMCO) is all of this and more.<br>
				<br>
				Apart from talent, she also has strong views on various 
				subjects, here are a few examples:<br>
				<b><br>
				If you had the power to change something in Oman what would that 
				be? </b><br>
				Youngsters feel strongly repulsed by the Vasta (influence) 
				culture in Oman. Vasta is a strong thing not just in Oman but in 
				the Arab world in general and this needs to change.<br>
				<b><br>
				Does Oman offer enough growth opportunities for youngsters? </b>
				<br>
				Oman does not offer enough avenues for young people because 
				there are not enough forums to get recognised. If you are not at 
				the top or do not get enough publicity then you cannot reach 
				anywhere.<br>
				<b><br>
				Are people ready to work hard in life to achieve their goals?</b><br>
				There are people in senior positions in the private sector who 
				just come to work for the sake of it – they have coffee, read 
				the newspapers and go home, people need to want to work and if 
				they have it in them then there is no stopping them.<br>
				<br>
				Call it confidence, determination or arrogance, this streak has 
				enabled Madiha to successfully foray into unchartered terrain. 
				As a Basma marketer she goes to industrial areas like Ghala, 
				where few women are seen. Says Madiha, “Initially people used to 
				say that as a lady you should not be going to such areas, but I 
				go there to market cards and I make my company good money. I 
				want to prove that I can do this job as well as any other man. I 
				like that challenge and want to prove myself.”<br>
				<b><br>
