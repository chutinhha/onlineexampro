				<font face="Verdana" size="4" color="#C00000">
				No quick fixes</font></b><font face="Verdana" size="1"><br>
				</font><b><font face="Verdana" color="#333333" size="2"><br>
				Hanna Al Kharusi’s dedication to every part of her job 
				irrespective of its importance or size holds a lesson for other 
				youngsters<br>
				</font></b><font face="Verdana" size="1"><br>
				<br>
				Have you never considered shifting jobs? That's something that 
				Hanna Al Kharusi, unit head, corporate banking division, 
				National Bank of Oman (NBO) gets asked quite often. “I joined 
				NBO in 2000 and still consider it to be my nesting ground, the 
				bank employed me when I had a degree unrelated to banking, 
				trained, invested and rewarded me. I have been recognised and 
				appreciated and I believe in loyalty towards an organisation,”
				<br>
				says Hanna. Well said. <br>
				<br>
				<b>Initial grounding</b><br>
				After doing her primary schooling in Oman, she moved to Holland 
				in class eleven as her father got posted to the Netherlands in 
				Hague. She honed her public speaking and communication skills in 
				school by participating in mock events like a model United 
				Nations, in which various schools represented different 
				countries. After school she proceeded to pursue a degree in 
				science from Indiana University in the US. <br>
				<br>
				On coming back to Oman Hanna looked around for a job related to 
				her science background but could not find anything challenging. 
				“It was then that one of my relatives suggested that I should 
				look for a job in banking, but since I had a background in 
				science I was a bit reluctant to take up a job in banking, but I 
				took it up as a challenge.” She applied at various banks and was 
				picked up by NBO to join their management trainee programme. 
				After a rigorous 12 month course she joined the bank’s corporate 
				banking division as an assistant relationship manager and has 
				got promoted as a relationship manager, senior relationship 
				manager and a unit head. <br>
				<br>
				Talking about her strengths she counts communication skills as 
				one of her biggest assets. Says Hanna, “This is something that I 
				acquired over the years, in schools there were shows like 
				show-and-tell, speeches, singing choirs and it is something that 
				I enjoy doing. Even in my job I do presentations for the higher 
				management and the board.” <br>
				<br>
				Apart from communication, commitment, dedication and a 
				willingness to take on even meagre jobs has stood her in good 
				stead. Hanna avers, “Nowadays there are a number of university 
				graduates who come out of college and want a managerial job 
				straight away, they need to realise that things do not work that 
				way.” She feels that a person who rises through the ranks, 
				learns over the years, which in turn gives him the confidence 
				and experience to excel. If someone is given things ahead of 
				one’s time, the person will not have the base, confidence and 
				experience to push forward.<br>
				<br>
				Speaking about young Omanis she says, “The new generation coming 
				into the workforce is quite ambitious, has a lot of potential 
				and a number of them have been educated locally, while during 
				our times, most people who used to go to a University came from 
				abroad as there was only SQU and two private colleges.” While 
				going abroad for an education has its advantages like giving 
				more exposure to different cultures and practices, a number of 
				local graduates have done equally well and so it is no longer a 
				key differentiator for success. <br>
				<br>
				Being a women has never been a constraint for her growth, on the 
				contrary it has helped her as people do not expect women to 
				deliver certain things but when they do so, it really shines. 
				Says Hanna, “Women need to work that much harder to prove 
				themselves, but there is no discrimination. In education there 
				is equality between men and women, but in the job market there 
				are certain industries which shy away from taking women as it is 
				a cultural thing, but things are changing and we hear about 
				women pilots, geologists and physicists and it is just a matter 
				of time before things change and women are accepted in these 
				kinds of jobs too.” <br>
				<br>
				She feels that the growth of Oman’s economy and development is 
				creating a lot of job opportunities for Omani youth and 
				youngsters should get a solid education, good communication and 
				interpersonal skills. As the competition gets tough education is 
				important so that one is on the same level field as everyone 
				else. <br>
				<br>
				Speaking about the kind of education that youngsters should aim 
				for Hanna says, “As long as you have a solid foundation, a work 
				ethic, reading skills and an ability to grasp things, it is not 
				essential to have a professional degree. While it helps to study 
				things that you can work around, it does not mean that if you do 
				liberal arts or science you cannot get into business or 
				engineering.” Finally it comes down to accentuating your 
				positives, identifying ones incompetencies and working on them. 
				Being a working women, wife and mother means that she is 
				constantly striving to strike a work-life balance – “It is 
				difficult and more so as one moves up as both ones job and 
				family are demanding, that’s a challenge that I am still working 
				to resolve,” says Hanna. </font>
