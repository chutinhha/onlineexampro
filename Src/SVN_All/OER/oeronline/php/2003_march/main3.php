              <font face="Verdana" size="4" color="#FF0000">The Good, The Bad &amp; The Ugly</font><p><b><font face="Verdana" size="1">Robert Bacal explores how conflict within organisations need to managed positively</font></b></p>
              <p><font face="Verdana" size="2">In my work with public sector managers and supervisors, the issue that generates the most emotion, and frustrated comments, is conflict within the organisation.  We generally do not look at conflict as opportunity - we tend to think about conflict as unpleasant, counter-productive and time-consuming. Conflict that occurs in organisations need not be destructive, provided the energy associated with conflict is harnessed and directed towards problem-solving and organisational improvement. However, managing conflict effectively requires that all parties understand the nature of conflict in the workplace.&nbsp;<br>
              <br>
              <b> Two Views:  The Good, The Bad&nbsp;</b><br>
 There are two ways of looking at organisational conflict.  Each of these ways is linked to a different set of assumptions about the purpose and function of
              organisations.<br>
              <br>
              <b>The bad&nbsp;</b><br>
              <img border="0" src="manage-ugly.jpg" align="left" width="200" height="152">  The dysfunctional view of organisational conflict is imbedded in the notion that organisations are created to achieve goals by creating structures that perfectly define job responsibilities, authorities, and other job functions.  Like a clockwork watch, each
              'cog' knows where it fits, knows what it must do and knows how it relates to other parts. This traditional view of organisations values orderliness, stability and the repression of any conflict that occurs. Using the timepiece analogy we can see the sense in this. What would happen to time-telling if the gears in our traditional watches decided to become less traditional, and re-define their roles in the
              system?<br>
              To the 'traditional' organisational thinker, conflict implies that the organisation is not designed or structured correctly or adequately. Common remedies would be to further elaborate job descriptions, authorities and responsibilities, increase the use of central power (discipline), separate conflicting members, etc.&nbsp;<br>
   This view of organisations and conflict causes problems. Unfortunately, most of us, consciously or unconsciously, value some of the characteristics of this
              'orderly' environment.  Problems arise when we do not realise that this way of looking at organisations and conflict only fits organisations that work in routine ways where innovation and change are virtually eliminated. Virtually all government organisations work within a very disorderly context - one characterised by constant change and a need for constant adaptation. Trying to
              'structure away' conflict and disagreement in a  dynamic environment requires tremendous amounts of energy, and will also suppress any positive outcomes that may come            from disagreement, such as improved decision-making and
              innovation.</font></p>
              <p><font face="Verdana" size="2"><b>The good<br>
              <img border="0" src="mangae-good.jpg" align="right" width="150" height="292"></b>The functional view of organisational conflict sees conflict as a productive force, one that can stimulate members of the organisation to increase their knowledge and skills, and their contribution to organisational innovation and productivity. Unlike the position mentioned above, this more modern approach considers that the keys to organisation success lie not in structure, clarity and orderliness, but in creativity, responsiveness and adaptability. The successful organisation, then, needs conflict so that diverging views can be put on the table, and new ways of doing things can be created.&nbsp;<br>
 The functional view of conflict also suggests that conflict provides people with feedback about how things are going. Even
              'personality conflicts' carry information to the manager about what is not working in an organisation, affording the opportunity to
              improve.<br>
              If you subscribe to a flexible vision of effective organisations, and recognise that each conflict situation provides opportunity to improve, you then shift your view of conflict. Rather than trying to eliminate conflict, or suppress its symptoms, your task becomes managing conflict so that it enhances people and organisations, rather than destroying people and organisations.&nbsp;<br>
 So, the task is to manage conflict, and avoid what we call 'the ugly' ...where conflict is allowed to eat away at team cohesiveness and
              productivity.<br>
              <br>
              <b>The ugly</b><br>
              We have the good (conflict is positive), the bad (conflict is to be avoided), and now we need to address the ugly.  Ugly occurs where the manager (and perhaps employees) attempt to eliminate or suppress conflict in situations where it is impossible to do so. You know you have ugly in your organisation when:</font></p>
              <ul>
                <li><font face="Verdana" size="2">many conflicts run for years&nbsp;</font></li>
                <li><font face="Verdana" size="2">people have given up on resolving and addressing conflict problems</font></li>
                <li><font face="Verdana" size="2">there is a good deal of private bitching and complaining but little attempt to fix the problem</font></li>
                <li><font face="Verdana" size="2">staff show little interest in working to common goals, but spend more time and energy on protecting
                  themselves</font></li>
              </ul>
              <p><font face="Verdana" size="2">When we get 'ugly' occurring in organisations, there is a tendency to look to the manager or formal leader as being responsible for the mess. In fact, that is how most employees would look at the situation.  It is true that managers and supervisors play critical roles in determining how conflict is handled in the organisation, but it is also true that the avoidance of ugliness must be a shared responsibility. Management and employees must work together in a cooperative way to reduce the ugliness, and increase the likelihood that conflict can be channelled into an effective force for
              change.<br>
              <br>
              <b>Ugly strategies</b><br>
              <img border="0" src="manage-bad.jpg" align="left" width="150" height="216">Let's look at some common strategies that result in the increase of ugly conflict. Most of the ugly strategies used by managers, employees, and organisations as a whole are based on the repression of conflict in one way or another. We need to point that, in general, you want to avoid these approaches like the
              plague.<br>
              <br>
              <b>Ugly 1: Non action</b><br>
              The most common repressive management strategy is nonaction - doing nothing.  Now, sometimes, doing nothing is a smart thing to do, provided the decision to do nothing is well thought out and based on an analysis of the situation. Most of the time, people
              'do nothing' about conflict situations for other reasons, such as fear of bringing conflict into view, or discomfort with
              anger.<br>
              Unfortunately, doing nothing generally results in conflict escalating, and sets a tone for the
              organisation...'we don't have conflict here'.  Everyone knows you have conflict, and if you seem oblivious, you also seem dense and out of
              touch.<br>
              <br>
              <b>Ugly 2: Administrative orbiting&nbsp;</b><br>
 Administrative orbiting means keeping appeals for change or redress always 'under consideration'. While nonaction suggests obliviousness since it doesn't even acknowledge the problem, orbiting acknowledges the problem, but avoids dealing with it. The manager who uses orbiting will say things like
              'We are dealing with the problem', but the problem never gets addressed. Common stalls include: collecting more data, documenting performance, cancelling meetings, etc.&nbsp;<br>
              <br>
              <b>   Ugly 3: Secrecy</b><br>
              A common means of avoiding conflict (or repressing it) is to be secretive. This can be done by employees and managers.  The notion is that if nobody knows what you are doing, there can be little conflict.  If you think about this for a moment, you will realise its absurdity. By being secretive you may delay conflict and confrontation, but when it does surface it will have far more negative emotions attached to it than would have been the case if things were more
              open.<br>
              <br>
              <b>Ugly 4:  Law and Order</b>&nbsp;<br>
 The final 'ugly strategy'.  Normally this strategy is used by managers who mistakenly think that they can order people to not be in conflict.  Using regulations, and power, the person using the approach
              'leans on' people to repress the outward manifestations of
              conflict.<br>
              Of course, this doesn't make conflict go away, it just sends it scuttling to the underground, where it will grow and increase its destructive power.&nbsp;<br>
   The notion that conflict should be avoided is one of the major contributors to the growth of destructive conflict in the workplace. The
              'bad' view of conflict is associated with a vision of organisational effectiveness that is no longer valid (and perhaps never was). Conflict can be directed and managed so that it causes both people and organisations to grow, innovate and improve. However, this requires that conflict not be repressed. Common repression strategies to be avoided are:  nonaction, administrative orbiting, secrecy and law and order.&nbsp;<br>
