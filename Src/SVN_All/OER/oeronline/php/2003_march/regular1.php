            was keen to stress. &quot;BP entered Russia five years ago when we bought 
            ten per cent of Sidanco. We had a tough time initially, but after 
            the present management and ownership structure was established early 
            in 2001 we have... learned a great deal about doing business in 
            Russia.&quot; <br>
