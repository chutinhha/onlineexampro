            two events that pose a direct and credible threat to the Middle 
            East's position as the dominant global energy provider (the region 
            satisfies more than 40 per cent of the world's daily oil needs). Oil 
            consumers are fighting back, launching multi-billion dollar 
            initiatives aimed at releasing the Middle East's stranglehold on 
            global energy supply. <br>
            First, on February 7, 2003, US President George W. Bush hosted a 
            high profile conference for hydrogen fuel developers. The guest list 
            included consultants, academics and, crucially, car manufacturers 
            such as General Motors and Toyota. President Bush pledged US$1.2 
            billion towards research into commercially viable fuel cells. <br>
            The move was a determined effort to put flesh on the bones of his 
            State of the Union address a few days earlier, during which he 
            pledged to slash America's dependence on overseas oil. &quot;Our goal is 
            to promote energy independence for our country,&quot; he told Americans. 
            For a country that is the world's number one energy consumer - and 
            Saudi Arabia's biggest customer - the implication is clear. <br>
            At the Washington hydrogen convention, the President was at pains to 
            demonstrate his enthusiasm for the fuel - and its potential to 
            replace oil. He posed using a hydrogen-powered mobile phone, and sat 
            behind the wheel of a hydrogen-powered car. <br>
            &quot;Hydrogen fuel cells represent one of the most encouraging, 
            innovative technologies of our era,&quot; said the President. &quot;And as I 
            said in my State of the Union address, the idea is to see that a 
            child born today will be driving a car which will be powered by 
            hydrogen.&quot; <br>
            He added: &quot;It won't be easy to get there because there are 
            obstacles. But I wouldn't be proposing this initiative if I didn't 
            think we could overcome the obstacles.&quot; If hydrogen does become 
            commercially viable, oil reserves could become worthless almost 
            overnight. <br>
            So much for the first, hi-tech threat to the Gulf's flow of 
            petrodollars. The second is closer to home and very much 'old 
            economy', but no less real. On February 11, 2003 British oil major 
            BP announced a massive US$6.75 billion oil development programme in 
            Russia, with local partner TNK/Sidanco. The bold move hammers home 
            the industrial world's resolve to find alternative sources of oil 
            outside the Middle East, should innovations such as hydrogen fail to 
            make it off the drawing board. <br>
            &quot;We believe this to be a great moment in the history of BP and of 
            TNK/Sidanco and, indeed, an important milestone in the history of 
            the industry,&quot; said BP chairman Lord Browne. <br>
