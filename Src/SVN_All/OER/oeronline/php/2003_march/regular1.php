            Russia. They feared that the oil was too expensive to produce, and 
            that Russia's political and economic instability posed too great a 
            risk. This was good news for Middle East producers, whose oil is 
            much cheaper to extract and who - for all their political 
            shenanigans - oil majors have come to trust over the past 100 years.
            <br>
