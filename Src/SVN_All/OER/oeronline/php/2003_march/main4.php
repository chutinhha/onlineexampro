            <img border="0" src="car-re-2.jpg" align="left" hspace="5" vspace="5" width="200" height="183">Beach companion</font><p><font face="Verdana" size="1"><b>Nayl D'Souza takes the 2003 Suzuki XL-7 for a test drive and discovers it is ready for family fun</b></font></p>
              <p><font face="Verdana" size="2">Essentially, the 2003 Suzuki XL-7 is the top-of-the-range model in Suzuki's sport-utility line-up which builds on the basic virtues of the Suzuki Grand Vitara. With a 12.6-inch longer wheelbase and an overall length that is more than 19 inches longer, the XL-7 offers much more interior space than the Grand Vitara. It also has a more powerful V6 engine than its smaller sibling and the midsized XL-7 offers more versatility by seating seven people. It has all the trimmings you would expect in a luxury medium-sized sport utility although the 2003 model adds only a few improvements to the car's interior from what was on offer last year. Apparently, no big changes are planned before a scheduled 2007-model makeover. Though that's a long way off, the next XL-7 looks set to be based on the redesigned 2004 Grand Vitara which is likely to retain truck-type body-on-frame construction, Suzuki V6 power, and dual-range 4WD.&nbsp;<br>
 At first glance, the XL-7's cuddly demeanour fills you up with the same mushy feelings most people get when they catch sight of a cute baby. Its smooth but chubby body is undoubtedly a major draw for some. Its simple interior looks and fair off-road performance gives it yet more youthful yet rugged charm for others. One can imagine it being used as a pose-mobile by those who favour driving something that conveys a sense of fun and adventure while also showing off slight flamboyance and independent
              spirit.<br>
              Perhaps the best selling feature of the XL-7 is how affordable it is. Starting from around RO7,495 (manual transmission), the car is competitively priced to fend off rivals such as the trendy Toyota RAV4, more-traditional SUVs like the Ford Explorer and GMC Envoy as well as more quirky but less family-orientated 4WDs like Wrangler Jeep. But, in terms of value-for-money, the XL-7 does exceedingly
              well.<br>
              <br>
              <b>Outdoors</b><br>
              <img border="0" src="carreview-1.jpg" align="right" hspace="5" vspace="5" width="200" height="135">The 4-door XL-7 has plenty of character. Its curving body style runs down the side and its bolder-is-better looks are put to use most at the front section of the SUV. Its stout appearance makes it stand sturdy next to its fellow sport utilities. You get power mirrors as standard as well as power windows, power door locks, and remote keyless entry. Halogen headlights and aluminium wheels are also fitted. The rear passenger doors are wider than normal, which helps improve the accessibility to the back area. However, the XL-7 is narrower than other midsize SUVs, so wide cargo items won't fit. Thank goodness the XL-7 comes with roof
              rails.<br>
              <br>
              <b>Indoors</b><br>
              Once inside the XL-7, you'll be surprised by how many attractive features it has. Enough to make you think this good-looking sport utility is a luxury sedan, claim Suzuki makers. I'm not sure I would go as far as thinking I had stepped into a Saab or Audi but the seat upholstery is very attractive on the base model and the steering wheel did tilt with amazing simplicity into a suitable driving position.<br>
              Suzuki has demonstrated a lot of thoughtfulness. For instance, both sun visors contain a vanity mirror and there are four good-sized storage compartments for the driver and front passenger (excluding the glove box) - at least two of which are designed to stow sunglasses. There are cupholders for front and rear passengers and a rubber portable ashtray in the centre console.<br>
              For 2003, the XL-7 moves further upscale with an all-new dashboard and console design with wood trim as well as a new CD player. The dual-zone climate control offers an automatic setting. Air from the unit is also dispensed from ducts by the centre console of the driver's area to keep rear occupants adequately cool too. Unfortunately, the three large dials to operate the air conditioning system have been styled by a fashion loony who has spoiled what would otherwise be a chic instrument panel that offers clear, simple, and handy
              controls.<br>
              The 2003 Suzuki XL-7 has standard seating for five people, with the option of a third-row seat for two additional people. They are split seats so if you need one more seat plus storage, it is easily accomplished. The total interior cargo space is around 75 cubic-feet which is fairly good in comparison to other SUVs in its class but only grocery bags can fit behind the third-row seat when used. Sitting in the rear seats would be rather cramped for a teen and I wouldn't be keen to seat children in the back of the Suzuki myself. There's good headroom in the second-row although it would be a tight squeeze for three adults even after they had taken advantage of being able to recline a few
              inches.<br>
              Leather seat upholstery, fog lamps, rear roof spoiler, sunroof are extras featured on the XL-7 special
              edition.<br>
              <br>
              <b>Performance</b><br>
              The XL-7 has a 2.7-litre V6, 24-valve, quad-cam engine pumping out 170 hp at 6,000 rpm. It offers 231 Nm of torque at 3,300 rpm. Estimated fuel consumption is 17 mpg measured during city driving. Fuel economy among the Suzuki SUV family is mostly admirable.<br>
              Given that it offers rear-wheel drive or 4WD, the XL-7 is ready considerable rough ride. It certainly made light work of moderate slopes and gentle wadi-crossings around Muscat. Like the Grand Vitara, the XL-7 features ladder-type frame           construction, low-range transfer case on 4x4 models as well as shift-on-the-fly four-wheel drive that can be activated at speeds up to 70 kph. It also has impressive hauling power. Unfortunately, OER could not test this SUV with full-load around Jebel Al Akhdar as Suzuki claims the XL-7 will tow up to 3000-pounds, much higher than other SUVs in its
              class.<br>
              The engine is very quiet at low speeds although it feels strained and noisy in some stages of high rpm. Tyre drone on most surfaces is not
              noticeable.<br>
              <br>
              <b>Safety</b><br>
              Standard safety features include dual front airbags, rear-door child safety locks, and an energy-absorbing steering column. Reinforced side-impact beams, a steel-reinforced passenger compartment, and integrated front and rear crumple zones also reinsure impact safety. Best of all, the XL-7 has ABS brakes as standard although the brake feel and performance is middle-of-the-road. The steering performs well at low-speed but cornering at high speed offers noticeable tyre scrub and body lean, perhaps a symptom of the truck-based chassis. Notwithstanding, the all-round drive is stable and
              comfortable.<br>
