                  <font face="Verdana" size="2">The GCC member countries have declared a three-year transitional period (from 2003 to 2005), during which the actual implementation of the customs union will be studied before removing certain barriers and making the union fully effective. As a consequence of the union, the GCC states are in the process of adopting the unified customs law as well as issuing unified executive regulations to implement the law. The authorities in each state, pending finalisation of the unified laws and regulations, have issued procedures to implement the union with effect from January 1, 2003. It is expected that during the initial weeks of the customs union, there would be implementation difficulties, which would be addressed by the authorities in consultation with each other.<br>
                  <br>
                  <b>Customs tariff</b><br>
                  The GCC states have approved a unified customs tariff of five per cent on CIF value subject to certain exceptions. This has resulted in the standard rate going up from four per cent to five per cent in Kuwait, Qatar and the UAE. The customs authorities have the power to carry out an assessment of the declared invoice price. The price of the goods would include the cost of selling until entering the port but would exclude costs of local transportation within the country and the applicable duties and taxes following the entry of goods. The price of the goods would include the value of utilising any patent, design, pattern or trademark of the goods if they are manufactured according the registered patent, design or
                  trademark.<br>
                  <br>
                  <b>Concessional tariffs</b><br>
                  A concessional tariff of two per cent has been approved for goods produced in Arab countries that are signatories to the agreement for facilitation and development of commercial exchange between themselves. The duty is to be further reduced by 0.5 per cent per annum until it reaches zero per cent at the end of
                  2006.<br>
                  <br>
                  <b>Higher tariffs</b><br>
                  The GCC states have approved a customs tariff of 100 per cent on imports of tobacco and its derivatives. It is also provided that if such duty at 100 per cent is less than the specific amount levied on a per unit basis, then such higher specific amount per unit will apply. All types of alcoholic drinks also attract the higher customs tariff of 100 per
                  cent.<br>
                  <br>
                  <b>Customs duty exemption</b><br>
                  A unified list of goods that would be exempt from customs duties, has been approved by the GCC states. The list comprises over 400 goods including basic foodstuffs, personal effects and used household items subject to certain conditions. Exempted goods include imports required for the manufacturing
                  industry.<br>
                  The GCC states have also approved certain organisations that would be exempt from paying customs duties. These include defence forces, diplomatic and consular missions and charitable organisations. Some of the GCC states were previously granting customs duty exemptions to projects of national interest. Such projects do not specifically appear in the list of unified exemptions approved by the GCC states (except if they qualify as manufacturing industry).  There have been instances of customs duty being collected on imports for such projects, despite specific country exemptions. The authorities are currently reviewing ways to address this
                  issue.<br>
                  Under the customs union, goods moving from free trade zones to other GCC states are also attracting customs duty. Previously, goods re-exported from a free trade zone to a GCC state outside the individual country in which the free trade zone is located was exempt from customs duty as it qualified as a
                  re-export.<br>
                  <br>
                  <b>Protection duties</b><br>
                  Each member state has been allowed to continue imposing protective customs duty as per the list approved for each GCC country.  This protective duty will apply during the transitional period of three years, at the expiry of which it is expected that the protection will cease. If the goods covered by protection are imported first through another GCC state in which the protective duty does not apply, then that country would levy only the normal five per cent duty with the final destination country, where the protection duty applies, recovering the balance of the
                  duty.<br>
                  <br>
                  <b>Prohibited and restricted goods</b><br>
                  The GCC states have approved a unified list of prohibited as well as restricted goods. In addition, an individual country-wise list of restricted goods has also been approved. The prohibited goods include narcotics, goods contravening the Sharia Law and goods imported from
                  Israel.<br>
                  <br>
                  <b>Drawbacks on re-export</b><br>
                  The GCC states have approved rules for refund of customs duties on imported goods, which are re-exported outside the GCC States. The value of goods intended for re-export should not be less than US$5,000 and the re-export must take place within a year from the date on which they were imported for the first time from a non-GCC country and on duty paid. The claim for refund of customs duties is required to be made within six months from the date of
                  re-export.<br>
                  <br>
                  <b>Temporary imports</b><br>
                  The GCC states have approved rules for temporary imports, which do not attract customs duty.  Customs duties are secured by a cash deposit or a bank guarantee as may be decided by the Director General of Customs. These goods
                  comprise:<br>
                  * Machinery and heavy-duty equipment required for executing Government and approved investment projects.  Such temporary imports can be for a period of six months extendable for a further similar period up to a maximum of three
                  years.<br>
                  * Goods imported for the purpose of further processing<br>
                  * Goods imported for stadiums, theatres and exhibitions; in this category, temporary imports are allowed initially for a period of three months extendable to not more than a maximum of six
                  months.<br>
                  * Machinery and equipment imported for the purpose of repairs.</font></p>
