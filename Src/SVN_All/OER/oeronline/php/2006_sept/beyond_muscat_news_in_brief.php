            <p align="left" style="margin-left: 5px; margin-right: 10px"><b><font face="Verdana" size="4" color="#FF0000">
            <br>
            </font><font face="Verdana" size="4">A Cynosure At Sohar</font></b></p>
            <p align="justify" style="margin-left: 5px; margin-right: 10px">
            <font face="Verdana" size="1"><b>A world class home and office 
            complex will meet the huge demands emerging at the industrial hub</b></font></p>
            <p align="center" style="margin-left: 5px; margin-right: 10px">
            <img border="1" src="sohar.jpg" width="350" height="122"></p>
            <p align="justify" style="margin-left: 5px; margin-right: 10px">
            <font face="Verdana" size="1">A multi-million dollar residential and 
            commercial complex, with world class amenities, will soon be the 
            toast of the Sultanate’s emerging industrial hub at Sohar in Batinah 
            region. The project is primarily meant to meet the burgeoning demand 
            for home and business offices — thanks to several huge petrochemical 
            units coming up there, with investments running into billions of 
            dollars.<br>
            <br>
            The agreement for the RO56 million ‘Sohar Residential and Commercial 
            Project’ has been signed between promoters Majan Gulf Properties (MGP), 
            a Bahrain-UAE joint venture, and International Gulf Development and 
            Contracting Company (IGDC), the construction company established by 
            MGP and the Lebanese Infrastructure Construction Company (ICC), in 
            the presence of HE Dr. Rajiha bint Abdul Ameer bin Ali, Minister of 
            Tourism, and HE Shaikh Mohammed bin Abdullah Al-Harthy, Minister of 
            Transport and Communication.<br>
            <br>
            Construction is scheduled to begin in September, and will be 
            completed in three years. The two-tier scheme has a furnished deluxe 
            apartment complex for homes and offices at an estimated cost of RO16 
            million, and six residential villa compounds at an estimated cost of 
            RO40 million. Al Bustan, the main residential and commercial centre, 
            will have five different blocks — including three residential blocks 
            featuring 260 apartments of different sizes. The office block will 
            have more than 6,000 square metres of open space offices, with banks 
            on the ground floor.<br>
            <br>
            The six villa compounds — Springs, Hills, Gardens, Meadows, Greens 
            and Prairies — will feature a fusion of Omani architecture with 
            contemporary Arab culture. The construction plan estimates an 
            additional 570 villas with eight different variations, ranging in 
            size from 210 square metres to 260 square metres.<br>
            <br>
            This project, designed and to be developed by MGP, represents the 
            first major project of its kind in the industrial wilayat of Sohar. 
            Commenting on the strategic location of Sohar for their first 
            endeavour, Mohammed Al-Marzougi, Chief Executive Officer, MGP, said: 
            “Sohar occupies a strategic 45 kilometre open seagate on the Gulf of 
            Oman and was chosen by the government to be developed into a main 
            industrial bay. The 200 square kilometre industrial city is home to 
            diverse industries — refineries, petrochemical plants and metal 
            manufacture.”<br>
            <br>
            Led by the government and with the aid of the local authorities, 
            Majan Gulf Properties is contributing to this industrial revolution 
            by creating residential and commercial synergy to keep pace with 
            industrial growth needs.”<br>
            <br>
            Several consultancy firms and contracting partners have joined hands 
            with MGP for this project and similar ones in the future, in Sohar 
            as well as in the wilayat of Khasab. The company has also developed 
            its own property management department in Sohar to ensure project 
            deadlines are met, which also ensures customer satisfaction. <br>
            <br>
            Other Sohar based premier MGP projects in the pipeline are The Pearl 
            sea resort located in Mejis, the Majan Logistic City (134,000 square 
            metres) facing the Sohar Industrial Zone, and the Khasab Family 
            Resort in Musandam. <br>
            <b><br>
            RO42 million tenders awarded</b><br>
            The Tender Board has awarded tenders 
            worth RO42 million. This includes RO12.2 million to establish the 
            infrastructure at Sohar Industrial Port, RO10.4 million to establish 
            a sea berth at Shannah in the Al Wusta region, a third one valued at 
            RO7.7 million to provide consultancy services to manage Seeb and 
            Salalah airports development projects, and a fourth project valued 
            at RO1.4 million to supply personal identity cards.<br>
            <b>
            <br>
            </b>
            <img border="0" src="bemuscat_1.jpg" align="left" hspace="5" vspace="2" width="153" height="75"><b>Sohar Refinery set to start </b>
            <br>
            Come September and the RO500 million Sohar Refinery will start 
            commercial operations. The second refinery in the Sultanate, this 
            refinery has a crude unit with 116,400 barrels per day (bpd) 
            capacity and a residue fluid cracking unit with a 75,260 bpd 
            capacity. Dr. Adil Abdulaziz Al-Kindy, CEO of Oman Refinery, said 
            that Sohar Refinery products would be exported only after meeting 
            the domestic demand. Driven by a high growth in domestic demand over 
            the recent months, this marks a significant change from the earlier 
            plan to export 90 per cent of the refinery’s products — which 
            includes gasoline, diesel and kerosene, all of which have a huge 
            demand in the international and domestic markets.<br>
            <b>
            <br>
            Aromatics factory at Sohar </b> <br>
            HE Dr. Mohammed bin Hamad Al-Rumhy, Minister of Oil &amp; Gas and Board 
            Chairman of Omani Aromatics Co; and Deputy Chairman of Oman Oil Co. 
            Board; recently signed the engineering and construction agreement 
            for the construction of the aromatics factory at Sohar Industrial 
            Estate in cooperation with GS Engineering and Construction Co. of 
            South Korea and LG International. Dr. Al-Rumhy stated that the 
            project was a new approach of Oman Oil Company to develop the 
            petrochemical sector in the Sultanate. He said that the project 
            would promote local investment and provide employment opportunities 
            to the Omani youth. The engineering and construction of the plant is 
            expected to commence in August and production would start in the 
            second half of 2008. <br>
            <b>
            <br>
            Plans firm up for Yiti
            and Shinas</b><br>
            The integrated tourist resorts at Yiti in Muscat Governorate and 
            Shinas on the Batinah coast are likely to get the green signal soon, 
            moving from the drawing board stage to on-site activity. Promoted by 
            Dubai Holding’s international property development arm, Dubai 
            International Properties (recently re-branded), total investment in 
            the two ventures is estimated at US$1 billion. Conceived as unique 
            lifestyle resorts with an Arabian flavour, the two resorts will bear 
            Salam Resort &amp; Spa as the new global brand name for Sama Dubai’s 
            planned chain of international tourism and leisure properties. The 
            resorts will feature a number of four and five-star beachfront 
            hotels — suitably designed to offer a combination of mountain, golf, 
            spa and beach resort experiences. There will also be 2,100 low-rise 
            residential units comprising 1, 2, 3 and 4-bedroom apartments.<br>
            <b>
            <br>
            Mega fabrication yard
            for Sohar Port<img border="0" src="bemuscat_2.jpg" align="right" hspace="5" vspace="2" width="150" height="82"></b><br>
            A major fabrication yard at Sohar will be put up by a joint venture 
            comprising Zubair Group (35 per cent) and India’s largest 
            engineering conglomerate, Larsen &amp; Toubro Limited (65 per cent). The 
            facility will be one of the largest of its kind in the Gulf region, 
            and will create — for the first time in the Sultanate — an industry 
            specialising in the construction of offshore structures, decks, 
            rigs, jackets and pile foundations. This project is of enormous 
            socio-economic significance for the Sultanate. First, it will be a 
            fillip to Oman’s entry to the competitive (driven by the 
            construction boom in the offshore oil and gas sector) offshore 
            supply market. Second, it will open up significant investment 
            opportunities for ancillary industries and support services in the 
            Sohar area. Third, it will generate employment on an immense scale.<br>
            <b>
            <br>
            Port Khasab expansion</b><br>
            The RO18 million expansion project of Port Khasab is meant primarily 
            to boost trade with Iran. Along with an Iranian trade area with 
            three floating platforms, it will have three principal service 
            sections — for fishing, local and foreign trade, and tourism. Over 
            the years, trade with Iran has shot up from 4,000 tonnes to 50,000 
            tonnes a year. Now, the expanded port facilities will be able to 
            serve the big boats and increasing trade services. At present, some 
            5,000 boats from Iran dock at Port Khasab every month, plus about 15 
            commercial ships from UAE and Iran.<i>— Aparajita Mukerjee</i></font></p>
