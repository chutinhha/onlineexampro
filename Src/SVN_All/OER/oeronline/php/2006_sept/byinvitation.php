<p align="justify" dir="ltr" style="margin-left: 10px; margin-right: 10px"><font face="Verdana" size="1">
<br>
<br>
</font><b><font face="Verdana" size="4">MSM : Is there light at the end of the 
tunnel?<br>
</font></b><font face="Verdana" size="1"><br>
<b>Nikhil Phutane looks at the current 
economic situation and its implications on the stock market</b><br>
</font><font face="Verdana" size="2">
<br>
</font><font face="Verdana" style="font-size: 11pt"><b>R</b></font><font face="Verdana" size="1">ising steadily over the past four years, and topping off with a crescendo in 
1HF ’05, the Muscat Securities Market seems to have reached its peak — with the 
Index dropping from 5700 to current 4697 level (17 July 2006) — a more than 15 
per cent fall — and leaving even its well-informed investors in the lurch. The 
low confidence level reflected in the stock volumes, and the increasing spreads 
in bid offer calls, is a stark warning for the months to come.<br>
<br>
<b>Economic linkage</b><br>
The oil price rise for the past three years has resulted in higher investment 
expenditures by the government — a higher allocation not only to the oil and gas 
industry but also on infrastructure spending. The direct beneficiaries under the 
MSM listed categories were banks, construction companies, and the hospitality, 
transport and port industries. Some of these benefits have also spilled over to 
the cement and building materials, leasing and power companies, which resulted 
from the higher disposable incomes along with increasing employment 
opportunities. Other allied sectors like tourism, real estate and industrial 
projects also have started reaping the benefits from these increasing 
expenditures. The gradual GDP rate of growth (5-6 per cent over the last 
four-five years) has made Oman a safer haven in the eyes of the investors. The 
rise in investments too has been gradual, as compared to the GCC countries. The 
current 1HF ’06 also has been a bumper year for the government, with the 
revenues expected to climb by more than 40 per cent on a year-on-year (YoY) 
basis. <br>
<br>
The supply-demand situation too continues to be favourable for oil producing 
nations, due to inadequate growth in refineries and political instability in 
some of the oil producing nations. However, the government wants to increase the 
GDP growth from non-oil revenues instead of depending all the time on oil. As 
supported by the directives of Vision 2020, this will help sustain investments 
in non-oil sectors of the economy like manufacturing, tourism and 
infrastructure, which has a large space on our listed market capitalisation.<br>
<br>
<b>More IPOs needed</b><br>
Presently, the market capitalisation of MSM is just about 40 per cent of the 
total GDP. But a kickstart to the IPO process can happen soon, with nearly RO 15 
billion investments projects — in conjunction with private players — either 
lined up (or shortly getting commissioned) in the industrial, infrastructure and 
tourism related projects. This will mark a huge turnaround in the IPO market 
which has largely been inconsistent and small, apart from few issues such as 
Omantel and Al Maha Marketing. <br>
<br>
While the interest rates have been increasing from 2005 and may hamper new 
projects, the rate of increase has been very slow and may well plateau — with 
rising oil prices generating further surplus for the government, adding 
liquidity to the market.<br>
<br>
<img border="0" src="byinvitation_1.jpg" hspace="5" vspace="2" width="500" height="202"><br>
<br>
<b>Fundamentals beckon</b><br>
Over the last four years, the Muscat Securities Market returns have closely 
tracked the manufacturing utilities along with the banks and a few service 
oriented companies — as they have constantly provided higher returns on 
incremental capital as against their cost of capital. If we compare the overall 
return on net worth across the manufacturing and service sector, as represented 
by the MSM Index, it has grown from 14 per cent (2002) to 18.6 per cent (2005) 
for the services sector; and from 7 per cent (2002) to 13.8 per cent (2005) in 
the industry sector. <br>
<br>
In fact, if one looks at the earnings growth, there has been a further 
improvement of 15 per cent in 1Q ’06 on a YoY basis. Moreover, there is a huge 
spread between the earnings yield of the entire market, which is presently at 
9.3 per cent on 1Q ’06 earnings as against a government paper that was recently 
auctioned at 4.2 per cent. This anomaly has to be corrected in due course if the 
earnings growth is to be sustained. As the chart (on page 64) indicates, this 
correction did start in mid-2005 and has boosted the valuations of the Omani 
Markets.<br>
<br>
<b>Changes yet to bear fruit</b><br>
The past 12 months has been taxing for the investors as the markets continued to 
stagnate, oscillating between the 4700-5700 levels. The huge upside during 1HF 
’05 did suggest that the markets could correct. But the extent (and more 
importantly, the time it is still taking) to revive — in spite of the sustained 
earnings growth and the huge amount of liquidity in the money markets — suggests 
that either the markets are not giving adequate risk-adjusted returns as against 
other avenues of local investments, or there’s an outside influence on the 
markets.<br>
<br>
So, what are the mega-project opportunities available for the Omani investor? 
Mostly, it has been real estate, construction equipment and other related 
services sector — forming the byproduct of a typical mega-project. The 
continuous rise in the real estate prices, in and around the Sohar belt and 
other tourism related areas, is a testimony of investments chasing better 
returns in the country.<br>
<br>
The other negative factor has been the continuous exit of foreign institutional 
investors (FIIs) from the listed companies over the past few months, in spite of 
very attractive valuations. This has again lead to poor sentiments prevailing 
amongst the local investors.<br>
<br>
The chart below shows that redemption pressure is still piling on, as indicated 
by the very low volumes (read as values), especially over the past few weeks. A 
third new dimension is the conflict in the Middle East region which has 
enfeebled the psyche of the investors. The situation, as of now, looks likely to 
escalate which could further depress the overall markets. However, we do not see 
any consequences of this imbroglio spilling onto the MSM markets in the medium 
term — as Oman is relatively a small market compared to other GCC markets where 
cross-holding between countries amongst the HNI investors is negligible. The MSM 
management too is trying hard to revive the situation by reducing the face-value 
of the scrips to create the appearance of greater liquidity — along with capping 
the frontline heavyweights stocks — so that a single company can not influence 
the entire market. By this, the management hopes to boost the positive 
sentiments of investors.<br>
<img border="0" src="byinvitation_2.jpg" hspace="5" vspace="2" width="500" height="196"><br>
<br>
<b>Will the sentiments improve?</b><br>
Based on the quarterly performances of the market over the past eight years, my 
analysis says that the second quarter normally decides the trend of the market. 
The last time the second quarter gave negative returns was in 2001, with the 
second half decisively ending up lower. However, during that time, the 
discounting for the markets had been more or less in the same range of today, 
with the sentiments also being bearish. <br>
<br>
Beyond 2006, it will again depend upon the sentiments, as the market valuation 
(referred to as Price/Earnings) reached its trough nine times — a whole 10-15 per 
cent lower than the current valuation. However, by the end of the year, the 
sentiments on the GCC markets could pick up — and this could stop the selling 
pressure in the local markets too. Already, in certain countries, signs of 
revival on the markets are being witnessed, while the real estate prices could 
either stabilise or show signs of weakening after two-three years of drastic 
increase.</font></p>

