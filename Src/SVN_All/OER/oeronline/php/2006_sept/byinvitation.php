<p align="justify" dir="ltr" style="margin-left: 10px; margin-right: 10px"><font face="Verdana" size="1">So, what should one do over the coming months? The best strategy for an equity 
investor is to buy with a long term perspective in stocks which could sustain 
their current prices on the following parameters:<br>
<br>
• Their earnings growth<br>
• Higher than existing incremental returns on capital employed as against their 
cost of capital.<br>
