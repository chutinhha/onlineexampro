			<font face="Verdana" color="#FF0000" style="font-size: 14pt">
            <br>
            </font><font face="Verdana" style="font-size: 14pt">Spudding History</font><font face="Verdana" size="1"><br>
			A well-chronicled pioneer’s tale in prose ’n photos</font></b><font face="Verdana" size="1"><br>
			<br>
			<img border="0" src="brow1.jpg" width="300" height="384" align="right">Books 
			that commemorate events or anniversaries have a purpose to serve, as 
			they are meant to cover that particular event or document a period 
			of history related to the people, place or institution — or, as in 
			this case, a commercial venture. Sometimes, such books end up being 
			more useful than just what they set out to do and, undoubtedly, 
			can’t be dismissed as mere ‘company paraphernalia’. If documented 
			well these books serve a larger role. They are storehouses of 
			historical information of the region. Hadn’t these pools of 
			undocumented history been tapped, they could have been lost forever 
			— and Schlumberger in Oman is one such book.<br>
			<br>
			In the foreword by His Excellency Dr. Mohammed bin Hamad bin Saif 
			Al-Rumhy, the Minister for Oil and Gas, praises the company’s 
			dedication and professionalism of the personnel through 
			Schlumberger’s 50 years of operations in Oman. The company’s role in 
			the Sultanate has been aptly summed up by the minister: “One of the 
			highlights of being part of this industry is the opportunity to form 
			partnerships with sophisticated organisations such as Schlumberger, 
			organisations that share a common vision for Oman’s long term 
			development. I am delighted that they are so obviously dedicated to 
			us. We need each other.”<br>
			<br>
			The chapters mark the chronological past of Schlumberger’s 
			operations in Oman. The early years, depicted in the opening 
			chapter, outline the challenges of remote operations — when 
			Schlumberger was deployed by the Petroleum Development of Oman as 
			well as the Cities Service Petroleum Corporation to log the first 
			wells. Hal Knudsen, who served as company representative to the 
			Dhofar Government in 1962, narrates the adventure of the very first 
			exploration. Ahmed Jama Jalaal describes a pristine culture 
			untouched by progress, all the more so due to the minimum 
			interaction of the local population with the oil industry employees.<br>
			<br>
			Then there’s Jacques Bonsergent reminiscing about the thrills and 
			chills of the remote location and small operation – how his 
			predecessor was held at gunpoint by Bedouin tribesmen, and how they 
			had to have a compulsory escort of two askaris. Peter Walmsley flew 
			into Oman in 1956, prior to the spudding of Fahud-I oil well, to a 
			small camp of about 70 expatriates and a few Omanis from the local 
			Duru tribe employed as guards and being paid in Maria Theresa 
			dollars. For Hugh Crocker, Muscat seemed little more than a small 
			fishing village. His most memorable part of the camp was the strong 
			Ryah Shamal (desert storm).<br>
			<br>
			The next chapter comprises recollections that chart the progress of 
			the company over a 30-year period. Omani staff recruited over the 
			years have risen through the ranks and now hold senior positions in 
			the organisation. The fascinating stories of these Omani and 
			expatriate engineers help the reader to visualise a country on the 
			threshold of being propelled into the modern world. The Renaissance 
			brought about under His Majesty Sultan Qaboos was undoubtedly 
			propelled by the oil industry’s earnings. Nevertheless, it was his 
			wisdom and progressive stance that has guided the country and all 
			ensuing enterprises.<br>
			<br>
			Salim Abdullah Al-Kalbani recalls, “I was 22 years old when I left 
			Oman. I travelled to Dubai — eleven days by camel — and from there I 
			took a dhow to Saudi Arabia. Dhows did not have engines then and it 
			took six days by sail to get to Dhamam.” He remembers he had to do 
			this like many other Omani men, as there was no work in Oman — a 
			situation being corrected by the Government’s Omanisation drive. 
			“During the sixties the Bedouin traveled mainly by camel, but in the 
			seventies they started to buy Land Rovers and became much more 
			mobile,” recalls Al-Kalbani.<br>
			<br>
			Khalfan Said Al-Ghafri recollects how important it was to involve 
			the local sheikhs to maintain peace in the remote rigs, and Abdullah 
			Al-Shukely strongly advocates the self-improvement and advancement 
			for Omani employees. With photos clicked by Andre Le Baron, Ulfat 
			Said Al-Suleimany (the first Omani woman to join Schlumberger) and 
			employees past and present, this book should be welcomed for 
			value-adding a lot of historical information by way of 
			Schlumberger’s pioneering act in the remote oilfields of Oman.<br>
			<br>
			The book moves onto The Job Today — real teamwork in real time. Here 
			a multi-racial group of employees has sent in their views, and one 
			can feel how the job has changed into team efforts in getting the 
			tasks completed. While a very technical chapter on logging and 
			measuring oil is a bit of a jolt to the well-rounded book, the last 
			— and most interesting part — of the book for non-oil industry folk 
			is the collection of pictorial reminiscences. It is fascinating to 
			see what these very places, where we live today, looked like just a 
			few years ago!<br>
			— Uma Devi Jadhav<br>
