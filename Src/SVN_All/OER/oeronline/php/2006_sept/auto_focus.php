        <p style="margin-left: 10px; margin-right: 10px" align="justify"><br>
        <font face="Verdana" style="font-size: 14pt"><b>“Business is only as 
        good as the people in it”</b></font><font face="Verdana" size="1"><b><br>
        Akshay Bhatnagar gets Justin Barnett, General Manager, Zubair Automotive 
        LLC, to talk about how much has he accomplished, as he completes one 
        year at the helm in Oman, and his strategy to take the company forward
        <br>
        </b><br>
        <img border="0" src="autofoc.jpg" align="right">What 
        do you say of a man who tells you that all auto markets in this world 
        are pretty much the same? You got to take him seriously if he happens to 
        be Justin Barnett, General Manager, Zubair Automotive LLC — with more 
        than 22 years of experience in global auto market managing brands 
        including Ford, BMW, Volvo, Rolls-Royce and Land Rover in Europe and 
        Middle East. On the manufacturer’s as well as the dealer’s side.<br>
        <br>
        Last month, Barnett completed his first year in Oman with Zubair 
        Automotive — managing auto brands including Mitsubishi cars, SUV and 
        light trucks, Mitsubishi Fuso trucks, Chrysler, Dodge and Yamaha 
        motorcycles. Talking of his experience here, the Zubair Automotive GM 
        says, “It’s been a fantastic year. A real pleasure working for a company 
        owned by the Zubair family, which believes in maintaining a fine balance 
        between preserving the Sultanate’s rich culture and demands of the 
        international markets to take the business forward.”<br>
        <br>
        Generally, many people find it challenging adjusting to the market 
        environment in Oman, especially when they come from Europe. But Barnett 
        didn’t find it tough. “I had my first taste of the Middle East in 
        Kuwait, where I worked before shifting to Oman. Moreover, I believe that 
        all markets are pretty similar. I don’t find any unique characteristic 
        in the auto market of Oman.”<br>
        <br>
        But Barnett doesn’t rest there. “Every market is probably pretty 
        similar,” he quips, and then comes the explanation. “Customer’s buying 
        attitudes are pretty much the same world over. What makes the market 
        different from each other is the business potential of separate product 
        segments, which varies from one market to another on the basis of the 
        consumer’s disposable income. If you compare Oman to other countries 
        within GCC, there are a greater number of smaller vehicles on the road 
        in the Sultanate.” <br>
        <b><br>
        Change in focus </b><br>
        Zubair Automotive has been paying greater attention to increase 
        Omanisation of its team. Recently, Zubair Enterprises Northern (part of 
        Zubair Automotive) was felicitated by the government for having the 
        highest Omanisation level in the automotive industry. <br>
        <br>
        “We are transforming our management style to create leaders in managers 
        who can play the role of coaches and mentors for their respective 
        teams,” points out Barnett. Explaining in his lucid style, he says, “We 
        are developing proper induction programmes and creating career paths for 
        our team members. As a result of our efforts we see a further increase 
        in enthusiasm in our staff’s attitude. Now they can foresee their future 
        as individuals, and of the company as a whole.” <br>
        <br>
        But how does it benefit the company and its customers? That’s an easy 
        job for Barnett. “I’m looking at a strategic growth for the company. And 
        that can be possible only if Zubair Automotive is recognised — not just 
        by our own customers but also by other’s customers too — as a place for 
        a pleasurable experience. Only a motivated team member can make this 
        happen.”<br>
        <br>
        So, it’s all sweet sales talk? The Zubair Auto GM starts pacing, more to 
        put his thoughts in focus. “No, I wouldn’t actually mind if somebody 
        doesn’t buy from us, for the simple reason that we don’t have the 
        product that suits his or her need. If someone comes to our place and 
        meets our staff, and leaves with a positive impression, then he or she 
        would recommend us to others. But if someone leaves with a bad 
        experience, then we have actually failed in our task. It’s very 
        important that we don’t let this happen.”<br>
        <b><br>
        Challenges ahead</b><br>
        With changing dynamics of the auto industry in Oman, how is Zubair 
        Automotive keeping pace with the new challenges? To answer this, Barnett 
        has to come back to his desk. “As a result of the buoyant economy, we 
        have seen a natural growth curve in the recent time, but we shouldn’t 
        become complacent. We must look after our existing customer base.”<br>
        <br>
        Would he care to elaborate? “Chasing a new customer is four times more 
        expensive than retaining an existing customer,” comes the fast return 
        from Barnett, a keen tennis player, and the next volley: “We have a very 
        healthy database of our customers, and we try to ensure that we look 
        after them so well, that they come back to us and stay loyal. That’s the 
        foundation of guaranteeing longevity in the business.”<br>
        <br>
        Now the Zubair Auto GM gets a chance to expound his catch ‘em young 
        philosophy — and he is at his flowing best. “Look, we have to look at 
        the demands of our customers and the manufacturers equally. The majority 
        of the population is under 25, with a different set of aspirations and 
        influences compared to the older generation. We have to keep pace and 
        support their requirements. Also, the population is spreading across the 
        Sultanate. So, unless one has facilities in various regions to support 
        the buying patterns, one is bound to lose out.”<br>
        <br>
        “Our national operations have been split into five regional offices, 
        each managed by a regional manager with complete responsibility and 
        accountability. We currently have 15 sales operations, and 24 service 
        and parts, located across the Sultanate. We also have a Quick Service 
        Centre in Athaiba that caters to all brands of cars. We are refurbishing 
        existing facilities in some locations, and we have acquired more land in 
        certain strategic areas to expand our network. Actually, if you look at 
        our facilities, they were ahead of the time when they were built. But 
        it’s time to refurbish and bring them up to the current standards,” 
        acknowledges Barnett. <br>
        <br>
        A few competitors have put up big showrooms with trendy designs. Is that 
        the reason for Zubair Automotive to take up this refurbishment and 
        expansion exercise, realising they are lacking on that count? Barnett 
        contests this question vehemently. “I won’t say that we are lacking. 
        What I would say is that facilities will not make your business. They 
        will, perhaps, help your business — but only to some extent. Jazzy 
        showrooms often fail to boost sales. Business is only as good as the 
        people in it. I have worked in environments where companies built brand 
        new showrooms. But the business didn’t increase.”<br>
        <br>
        “They lifted the employees from the old showroom to the new showroom, 
        but nothing changed. They have not put in place the people element — 
        with no adequate training or motivational measures — to drive the 
        business.”<br>
        <br>
        Yes, Barnett has sure scored his point, and now comes the punchline. 
        “It’s nice to go to a pleasant environment. But that’s not going to make 
        or break your business. That comes from how you handle the customers. If 
        you actually look at our strategy, in its simplest form it’s about 
        having the right people in the right premises. It’s about the right 
        process and procedures for them to understand and follow, so that they 
        can deliver customer satisfaction. It’s about having the right computer 
        system in support, so that we can get the logistics right. The right 
        vehicles in the right places at the right time.”<br>
        <br>
        “Finally, it is about your whole team having a genuine desire to 
        achieve. These are the fundamentals of how to drive your business, the 
        rest follows. We do acknowledge that we need to upgrade some of our 
        facilities and processes, and that’s underway now. We have a new 
        facility construction going on in Salalah, and there are other projects 
        which are about to start,” Barnett wraps up with a flourish.</font></p>
