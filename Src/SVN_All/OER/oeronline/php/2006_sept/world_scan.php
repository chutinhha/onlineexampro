<p align="justify" style="margin: 3px 10px"><p align="justify" style="margin: 3px 10px">
<font face="Verdana" size="1">
<img border="0" src="coke.jpg" align="left" hspace="5" vspace="2" style="border: 1px dotted #C0C0C0; padding-left: 4px; padding-right: 4px; padding-top: 1px; padding-bottom: 1px" width="182" height="251"><b>Coke, Pepsi partially banned in India </b><br>
<br>
A number of states in India have banned the sale or manufacture of Coke, Pepsi, 
Sprite and other soft drinks made by the Indian subsidiaries of Coca-Cola Co. 
and PepsiCo Inc. The ban has been enforced after a research group in New Delhi 
claimed the soft drinks contained high levels of pesticide residue. The move has 
affected the local sales of Coca Cola and PepsiCo, which account for nearly 80 
per cent of India’s US$2 billion-plus soft drinks market. The companies have 
denied the allegations in a joint statement, stating that both “comply with 
stringent international norms and all applicable national regulations.”<br>
<br>
The controversy started when the New Delhi based Centre for Science and 
Environment (CSE) said that it found pesticide residues in the samples of Coke 
and Pepsi that were 24 times above the limits set by the Bureau of Indian 
Standards (BIS). The CSE said it carried out tests on 57 samples taken from 11 
soft drink brands made by Coca-Cola India and PepsiCo India and found a 
“cocktail of three to five different pesticides,” all apparently present in 
groundwater used to make the drinks. The toxins could, if consumed over a long 
period, cause cancer, damage to the nervous system, birth defects and disruption 
of the immune system, the CSE said. As per the US media reports, the ban could 
impact the flow of investment from US to India. </font></td>
