China Mobile also claimed to be the world’s largest mobile phone operator ranked 
by their number of subscribers. It has more than 200 million customers, while 
the most recent figure from Vodafone shows 186.8 million worldwide. While 
Vodafone’s turnover is still ahead of China Mobile, it has struggled to add 
customers in a competitive European marketplace, with its UK customer base 
falling by 119,000 in its most recent quarter. On the other hand, China Mobile 
has nearly 40 per cent of the Chinese market, while analysts expect it to expand 
its presence overseas.</font></td>
