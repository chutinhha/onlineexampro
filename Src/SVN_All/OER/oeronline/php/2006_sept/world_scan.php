<b>China Mobile overtakes Vodafone <br>
</b><br>
<img border="0" src="china_mobile.jpg" align="left" hspace="5" vspace="2" style="border: 1px dotted #C0C0C0; padding-left: 4px; padding-right: 4px; padding-top: 1px; padding-bottom: 1px" width="218" height="190">Vodafone 
has lost its crown of the world’s No. 1 mobile services company to China Mobile 
