<br>
Matein Khalid talks about how a strong bond market can finance the Arab world’s 
infrastructure needs<br>
</b>
<br>
<b>
<img border="0" src="pirscope.jpg" align="left" hspace="5" vspace="2" width="80" height="105"></b></font><font face="Verdana" style="font-size: 11pt"><b>A</b></font><font face="Verdana" size="1">rab bond markets are a miniscule and illiquid niche of the intermediate debt 
market, despite such seminal innovations as Sharia compliant debt (sukuk ijara), 
sovereign Arab Eurobonds and bank Floating Rate Notes (FRNs). New issues, 
secondary markets’ depth, research coverage, trading volumes, bid offer spreads, 
investment bank underwriting and syndications capabilities, interest rate risk 
hedging instruments (such as futures, options and swaps), sophistication of 
regional institutional investors in credit risk analysis and duration risk 
management, rating agencies coverage in the Arab world are still embryonic — 
compared to their peers in the West or even major Asian economies.<br>
<br>
For instance, local currency bonds exist in Egypt or Kuwait. But these debt 
instruments often are not held in regional portfolios, and a mere handful of 
Arab bond mutual funds are in existence. Yet, the Arab world needs the evolution 
of debt capital markets to realise its economic development potential. The World 
Bank estimates that US$500 billion is needed to finance the Arab world’s 
infrastructure needs in the next decade. Bank finance alone cannot provide this 
scale of long term development finance in the GCC, let alone the Arab world.<br>
<br>
Moreover, the lack of sophisticated, robust bond markets in the Arab world 
exposes the region’s pension funds, insurance companies and family office to 
extraordinarily high market risk — because they do not have long duration debt 
instruments to balance their equities exposure during times of stock market 
stress.<br>
<br>
There are, of course, tangible reasons for the marginal, illiquid nature of the 
Arab debt markets. The region’s oil wealth ensured GCC governments are net 
creditors, not debtors in the international capital markets. Another reason is 
that business is dominated by state owned companies or private family owned 
conglomerates, who are reluctant to seek credit ratings or subject their 
finances to the standards of scrutiny necessary in the global debt markets.<br>
<br>
The regional banking system does not have a culture of credit risk underwriting. 
Bank loan books are dominated by ‘name lending’, and credit risk is priced 
arbitrarily in the financial markets, making bond market issuance unattractive 
to cheap bank finance where clients are not subject to balance sheet disclosure. 
Since oil exporting governments do not finance budget deficits via bond issuance 
(in the UAE, for instance), no benchmark yield curves have developed in key GCC 
states and corporate debt issuers are nonexistent. It is vital to encourage the 
evolution of bond markets in the Arab world. Bonds offer long time financing 
that is required by most GCC projects in power, energy infrastructure or 
petrochemicals — but tenors over 10 years are simply not possible in bank loan 
books. While bond finance increases the flexibility of debt management for 
sovereign and corporate treasurers, bond markets broaden the spectrum of risk 
management innovations in the capital market — including interest rate swaps, 
futures, options and credit derivatives. The advantages are three-fold:<br>
<br>
• Asset securitisation can act as the catalyst for a sophisticated real estate 
market, with ‘pay through’ and ‘pass through’ debt mortgage backed securities.<br>
• Bond markets enable GCC central banks to implement monetary policy via the 
open market operations.<br>
• Bond markets can reduce the need for Arab corporates to be dependent on bank 
finance alone.<br>
<br>
In fact, bond investment can reduce the volatility of Arab investor portfolios. 
Had they been in viable existence, government bond markets could have acted as 
class asset haven during the recent regional stock market crashes. Moreover, 
issuance of debt instruments like Euro-MTNs can enable banks to diversify their 
funding base and reduce the asset-liability mismatch on their balance sheets.<br>
