            <img border="0" src="beyond_3.jpg" align="left" hspace="5" vspace="2" width="150" height="72">Environment Protection</b><br>
            In keeping with local and international guidelines, OPPC has 
            inducted precision control filters to achieve zero emission levels, 
            and an effluent treatment plant and scrubbers to neutralise wastes 
            through proper disposal. The plant is landscaped with trees and 
            green lawns to provide maximum environmental protection.<br>
            <br>
            <b>Exports to MENA countries</b><br>
            OPPC has established high repute from its exports to the Middle East 
            countries such as Iraq, Libya, Syria, Yemen etc.<i>— Uma Devi Jadhav</i></font></p>
