            <br>
            <b>Which are the larger pharma plants in the GCC?</b><br>
            We are one of the large facilities in GCC. The other large 
            facilities in GCC are Gulf Pharmaceutical Co (Julphar) based in the 
            UAE, and Spimaco of Saudi Arabia, producing formulations.<br>
            <br>
            <b>How do you train your workforce?</b><br>
            We have consultants from the US, Germany, Switzerland and India 
            every two-three months to impart training to all our employees. We 
            also hire the services of Oman based trainers to train the Omani 
            staff at our facility. Similar training has also been provided to 
            Ministry of Health (DGPA&amp;DC) Quality Control staff.<br>
            Where do you get your raw materials from?<br>
            <br>
            As per the regulations of Ministry of Health (MoH), all the raw 
            material producers need to be approved before importing any 
            materials. We have to submit all the information such as CGMP 
            status, COA, impurities profile, stability data from the supplier 
            (producer) — and only after evaluating the information, MoH allows 
            us to import materials. At times, MoH visits the producer of raw 
            materials and audits their facilities before granting the No 
            Objection Certificate.<br>
                  <b>
            <br>
            Why would multinationals want to come here to give contract 
            manufacturing? </b> <br>
            Oman has a very good infrastructure, and low labour-energy costs and 
            taxes, as compared to Europe. All expertise is available from the 
            Indian Subcontinent at lower costs as compared to Europe. Moreover, 
            Oman has strict regulatory control, less hassles with regard to 
            other commercial regulations and the pharmaceutical regulations are 
            based on European guidelines. Hence, it becomes very attractive for 
            European companies to outsource products from Oman.<br>
                  <b>
            <br>
            Where is your machinery from? Is it second hand or brand new? </b> <br>
            All brand new machines sourced from Europe, the US and India. The 
            critical machines are from Europe, testing machines from the US, and 
            the supporting machines and material handling equipment from India.<br>
                  <b>
            <br>
            Why is your plant in Salalah? Do you get any subsidies for this 
            location? </b> <br>
            Salalah has an ultra-modern port having very good connectivity with 
            all important ports. The weather too is not very hot. And we get no 
            special subsidies at Salalah.<br>
            What’s the advantage for Belgium PSI and Necura of Switzerland to go 
            for tech-transfer with OPPC? <br>
            <br>
            These parties can outsource their businesses to us at lower costs. 
            Cipla has an advantage of selling their product to GCC by joining 
            hands with us.<br>
                  <b><br>
            What are the export potentials for OPPC?</b><br>
            We are exporting to Yemen since December 2005. We have been approved 
            by the GCC central committee, and Yemen is a member of this 
            committee.<b><br>
