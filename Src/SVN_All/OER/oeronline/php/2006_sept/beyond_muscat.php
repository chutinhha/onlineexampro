            </font><font face="Verdana" size="4">
            Rx Oman Pharmaceutical Products Co. LLC</font></b><font face="Verdana" size="1"><br>
            <br>
            <b>With an investment of US$50 million, Salalah-based Zynova (OPPC) 
            is going global with hormonal &amp; generic drugs, tech-transfers &amp; 
            joint ventures, plus registration services back-up for any and every 
            country<br>
            </b><br>
            <img border="0" src="beyond_1.jpg" align="right" hspace="5" vspace="2" width="200" height="160"></font><font face="Verdana" style="font-size: 11pt"><b>T</b></font><font face="Verdana" size="1">he pharmaceutical market in the Gulf region is on a fast track, 
            growing at 10 per cent per year (almost twice that of the global 
            market) and with an annual turnover of US$4 billion plus. Saudi 
            Arabia and United Arab Emirates alone account for about 85 per cent 
            of the total pharmaceutical sales in the Gulf Cooperation Council (GCC) 
            countries. For many years a strong public market characterised the 
            markets in the Gulf region, with the governments being the main 
            purchasers of drugs through tenders.<br>
            <br>
            However, in recent years, the importance of the private market has 
            increased dramatically. For two reasons: First, a large proportion 
            of the expatriate labour force in many of the GCC countries are 
            excluded from state health care. Second, those seeking treatment 
            abroad — in Europe or in the US — are now seeking treatment in the 
            region at high-class private clinics and hospitals operated by 
            expatriate doctors and surgeons with high qualifications from Europe 
            or the US.<br>
            <br>
            Quite a few of these clinics and hospitals have opened their doors 
            lately, and are attracting patients <br>
            <br>
            even from the Western countries for specific treatments. Another 
            strong reason is the move away from common disease pharmaceuticals, 
            such as antibiotics and vitamins, to medicines for diabetes, 
            cardiovascular diseases and oncology.<br>
            <br>
            Oman Pharmaceutical Products Co. LLC - Zynova (OPPC) is one such 
            entity which is taking advantage of this golden opportunity. OPPC’s 
            ultra modern plant is a business unit of Al Bahja Holdings in 
            Muscat, chaired by Ajit K Hamlai, an Omani national. Located at 
            Raysut Industrial Estate, Salalah, the plant is at par with the best 
            in the world.<br>
            <br>
            The fully integrated manufacturing facility offers a broad basket of 
            popular products, strong product development capabilities and all 
            encompassing emphasis on total quality management. The design 
            concept for the facility incorporates the latest technology in terms 
            of layout, flow of men and material, heat ventilation and air 
            conditioning (HVAC) systems and the treatment of water. The facility 
            complies with all international regulatory guidelines and the 
            comprehensive General Manufacturing Practices (cGMP) certification.<br>
            <br>
            This facility is capable of producing oral solids (tablets, 
            capsules, dry powder for oral suspension), oral liquids 
            (suspensions, syrups and elixirs) as well as ointments, creams and 
            gels. A unique feature of this plant — the only one of its kind in 
            the Gulf region — is the dedicated area for manufacturing hormones.<br>
            <br>
            Apart from developing and manufacturing generic and over-the-counter 
            (OTC) medicines, the pharmaceutical facility also offers contractual 
            manufacturing services to multinational corporations and large 
            pharmaceutical organisations wanting to outsource their production. 
            Such alliances are part of OPPC’s strategy to supplement its 
            expertise through partnerships, co-promotions and licensing 
            agreements. <br>
            German accreditation<br>
            <br>
            Oman Pharmaceutical Products Co. LLC is accredited to the Government 
            of Upper Bavarian Authorities, Germany as well as to the Ministry of 
            Health, Oman and the GCC Central Committee for Drug Registration. 
            “The German accreditation allows us to export to the European Union, 
            because of the Mutual Recognition Treaty among the European Union. 
            We have successfully cleared their audit in January this year, and 
            received the certificate,” says General Manager Rajendra H Bhandari 
            (See interview on page 47).<br>
            <br>
            With an investment of US$50 million, the 32,800 square metre 
            pharmaceutical plant — inaugurated in January 2004 — will employ 250 
            personnel from local and foreign nationalities when it becomes fully 
            operational. TQM (total quality management) is the watchword and a 
            way of life for the workforce. Sophisticated quality control 
            laboratories with the latest computerised analytical equipments 
            control and test every stage of formulation development and 
            production — from raw material stage to the finished and packed 
            products — and carry out in-process testing too. Special emphasis is 
            laid on validation, stability studies and bio-clinical studies.<br>
            <br>
            Complementing the instruments is a dedicated team of highly 
            qualified and experienced scientists. Training and upgradation 
            programmes keep the employees abreast of trends and demands in a 
            dynamic regulatory environment.<br>
            <br>
            <b>Technical Documentation for Registration</b><br>
            Today’s fiercely competitive and highly regulated markets demand 
            rigorous design and management of product development and 
            registration strategies for success. OPPC’s team of highly qualified 
            scientists and technical professionals have been made part of the 
            regulatory process — thus saving critical time in getting the 
            product to the market, in optimum period, for submission to the 
            regulatory authorities.<br>
            <br>
            <b>The Quality Assurance and Documentation Department provide 
            services including:</b><br>
            • Registration in Oman, GCC, European, United States Food &amp; Drugs 
            Authority (USFDA), as well as to the rest of the world. <br>
            • Technical package containing pharmaceutical, clinical and 
            therapeutic updates. <br>
            • Site master file, abbreviated applications and dossiers. <br>
            • Liaison with regulatory authorities for audits, inspection and 
            technical queries.</font></p>
            <p align="justify" style="margin-left: 5px; margin-right: 10px"><font face="Verdana" size="1">
