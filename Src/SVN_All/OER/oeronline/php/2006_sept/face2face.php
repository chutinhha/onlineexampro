<p align="justify" dir="ltr" style="margin-left: 5; margin-right: 5"><b><br>
</b><font face="Verdana" style="font-size: 14pt"><b>‘PCs are going to be 
obsolete’</b></font><font face="Verdana" size="1"><br>
<b>Tomio Isogai, Managing Director of Sharp Middle East FZE, in a chat with 
Akshay Bhatnagar, talks about how Sharp is moving ahead to reach RO10 billion 
net sales this year<br>
<br>
What’s 
the core business philosophy of Sharp? </b><br>
<b>
<img border="0" src="face.jpg" width="100" height="129" align="left" hspace="5" vspace="2"></b>Since our founding in 1912, Sharp Corporation has continuously opened up new 
areas of industry with its original products — from the “Ever-Sharp” mechanical 
pencils from which the company name was derived, to the commercialisation of the 
first Japanese-made radios and televisions, the world’s first LCD electronic 
calculators, and a host of other products incorporating our state-of-art LCD 
technology. These products have contributed to the improvement of living 
standards and the advancement of society as a whole.<br>
<br>
Now, the electronics industry is rapidly changing as information technology and 
network infrastructure advance, along with the acceleration of technological 
innovation. Companies worldwide are furiously competing to capture new markets. 
By once again returning to our business creed of “Sincerity and Creativity” — 
and being an innovator of products and services — Sharp is aiming to become an 
‘one-of-a-kind’ company that creates 21st century lifestyles with electronics 
technology.<br>
<b><br>
How is Sharp doing globally?</b><br>
Sharp posted record high sales for the third successive year in fiscal 2005. 
Consolidated net sales amounted to RO9.262 billion, up 10.1 per cent over the 
previous year, as a result of sales growth in core products — notably LCD TVs 
and mobile phones, combined with strong sales of key devices such as LCDs and 
solar cells. (See graph for Sharp’s financial performance in the last 5 years).<br>
<b><br>
How are you gearing up to optimise the surge in global market opportunities?</b><br>
In consumer and information products, we have enhanced our line-up of large size 
high definition LCD TVs — in anticipation of the genuine advent of the digital 
high definition TV era. Elsewhere, we have worked to enhance our range of high 
value-added products, such as mobile phones equipped with our original, 
cutting-edge device — thus aiming for higher profit. In electronic components, 
we have expanded our production capacity at the Kameyama (in Japan) No.1 plant 
stepwise to meet the burgeoning demand for LCD panels for TVs, and also started 
construction of the Kameyama (in Japan) No.2 plant. <b><br>
<br>
What are the key challenges before the company?</b><br>
In the medium term, we foresee a couple of challenges to realise stability in 
growth. We need to secure competitive advantage in the global market by 
strengthening the development that incorporates our unique technology, and 
accelerate creation of stand-alone products befitting the new era. We are 
committed to achieving stable growth in the future by enhancing our brand value 
globally. Sharp is also focusing on establishing an efficient value-chain which 
enables production within a short timeframe — at low cost and promoting global 
procurement activities. <br>
<b><br>
What’s your market share in the Middle East &amp; North Africa (MENA) region?</b><br>
It varies from market to market. On the whole, we have plenty of scope for 
growth — especially in the emerging markets. Yes, in some of the countries of 
the region Sharp has not kept pace with the local market growth rates. But MENA 
is an important market in Sharp’s target to cross RO9.932 billion net sales in 
the current year. We also see huge business potential in the replacement 
segment, where consumers upgrade to modern technology products from the 
conventional technology products. <br>
<b><br>
What steps are you taking to strengthen the presence of Sharp in MENA market?</b><br>
We have a three-fold business expansion strategy for the region. First, we are 
planning to introduce new products in consumer electronics and business 
equipments — new technologies not introduced in the region before. Second, 
provide information about our superior technology and its benefits to the 
consumer more aggressively. Third, train our local partners in key areas 
including customer sales and after-sales technical service to ensure perfect 
satisfaction to Sharp consumers. <br>
<b><br>
What’s the focus of your R&amp;D in terms of futuristic technologies?</b><br>
Our R&amp;D team is developing many new technologies. The most important one is 
about the integration of communication, broadcasting and home networks. It 
involves connecting all the key devices of communication, broadcasting and 
information on a single platform. For example, you are watching a programme on 
the satellite TV and wish to download some information related to that 
programme. Just with the touch of a button, you can download and store this in 
your personal computer (PC) or any other storage device. It means PCs are soon 
going to be obsolete. Your TV can be workable as a PC. </font></p>
<p align="center" dir="ltr" style="margin-left: 5; margin-right: 5">
<img border="0" src="face1.jpg" width="350" height="264"></p>
<p align="justify" dir="ltr" style="margin-left: 5; margin-right: 5">
<font face="Verdana" size="1"><b><br>
But how’s this integration process unique to Sharp — many of your global 
competitors are working in the same direction? </b><br>
Others may also be working on it, but we have the strong competitive advantage 
in some of the areas — like the LCD display, or in other technologies like 
semi-conductors. Our long experience in the development of related technologies 
gives us an edge over others in the development of integration process and 
devices.<br>
<b><br>
So when is all this going to reach the MENA countries?</b><br>
The sooner the better... This technology will soon be unveiled in Japan — and if 
it becomes successful and proves to be feasible, it will be on its way to the 
region in matter of one year probably. We see huge potential for the application 
of this integration technology in segments including new townships (like The 
Blue City and The Wave) and the hospitality industry (hotels and resorts). <br>
<b><br>
Why don’t we get to see many Sharp mobile phones in the region?</b><br>
Yes, it’s a shame for us not to see many Sharp brand mobile phones in this 
market. But let me point out that in just three years with our advanced 
technologies, we have moved to the top slot (in 2005) in the Japanese domestic 
mobile market. <b><br>
<br>
What’s stopping you from aggressively selling mobile phones in the MENA market?</b><br>
We don’t have enough production capacity like Nokia or Motorola. <br>
<b><br>
Does it mean that Sharp underestimated the market potential for mobile phones 
and failed to build adequate capacities in advance?</b><br>
Not really. Our strategy has been different. We have focused on Japanese mobile 
market and introduced advanced mobile products to meet the requirements of the 
customers successfully. But in the global market, other players have moved 
faster than us and built capacities to ship large volumes — even as we were 
working on new technologies for other product categories. We are now giving more 
thrust to our mobile business, and plan to sell 13 million units in 2006 
globally. <br>
<b><br>
What’s the mechanism you have to manage any friction between the different local 
partners in the region?</b><br>
Generally, we don’t face any problems from our local partners. We have been 
organising a get-together for our business partners in the region on an annual 
basis, where they get to know and interact with each other. It also serves as an 
excellent opportunity to share areas of conflicting interests, and look for an 
amicable solution with the help of Sharp, if required.<br>
<br>
Oman Modern Electronics Company and Sharp Corporation, Japan, have an 
association that goes back 30 years. Part of the Al Hashar Group, it is the sole 
distributor of Sharp brand products — including Aquos LCD TV, refrigerators, air 
conditioners, air purifiers and photocopiers etc in Oman.</font></p>

