<b>Creating Landmarks</b></font><font face="Verdana" size="1"><br>
<b>The infrastructure development giant, Carillion Alawi LLC, is strengthening 
its resources to play a larger role in Oman’s accelerated pace of growth. OER 
profiles the company<br>
<br>
</b>
</font><font face="Verdana" style="font-size: 11pt">
T</font><font face="Verdana" size="1">he smile on the cheery face of David J. 
Skinner has become much broader in the last few months. Gone are the days of 
uncertainty for him. With Oman’s economy in a buoyant phase, he is busy 
marshalling his 1850-plus workforce in managing more than a dozen projects 
spread across the Sultanate of Oman. As the regional manager of Carillion Alawi 
LLC (earlier known as Tarmac Alawi LLC), responsible for the company’s business 
in Oman, he is keeping the shareholders more than happy with contracts worth 
RO30 million in the company’s kitty.<br>
<br>
Business is expanding for Carillion Alawi LLC, the joint ownership company of 
Alawi Enterprises LLC (founded by late Qais bin Abdul Munim Al-Zawawi, Deputy 
Prime Minister of Oman) with 51 per cent stake — and rest of the 49 per cent in 
the hands of UK-based Carillion plc (annual sales of around US$7 billion and 
over 40,000 employees globally). <br>
<b><br>
Partner in progress</b><br>
Established in 1979, Carillion Alawi has managed major projects in Oman as a 
building, civil engineering, mechanical and electrical contractor. Skinner, in 
his second innings with the company since 2000 (first stint was between 
1992-1998), recalls, “Earlier, there used to be very few major infrastructure 
development projects in the Sultanate. But the scenario has changed dramatically 
in the last few years, especially in 2005-2006, with a number of multi-million 
Riyal projects coming up in different parts of Oman.” <br>
<br>
Talking about the company’s contribution towards Oman’s development, he says: 
“Despite lean phases of business, we have stuck to our commitment towards the 
country. And in the process, we have done some very prestigious projects such as 
Rusayl Power Station, Oman Cement Company Housing Complex, Fort Jalali-Muscat, 
Royal Hospital, Asma bint Qais Zawawi Mosque, Oman International Bank 
Headquarters, Salalah Container Terminal Project, Sultan Qaboos Port 
Development, Sultan Qaboos Grand Mosque, Muscat City Centre and multiple 
projects for Petroleum Development Oman, the Armed Forces and Government 
Ministries. All these activities have led to the development of infrastructure 
related to a wide range of industries to catalyse the economic and social 
development in Oman.”<br>
<b><br>
Beyond Muscat</b><br>
Though Carillion Alawi has undertaken many projects outside Muscat area, its 
focus has always been in and around Muscat. Skinner clarifies, “We have 
previously worked in Salalah (container terminal) and Sur (civil works for 
trains 1 and 2) besides other regions. Currently, we have contracts connected 
with highly prestigious projects such as Sohar Aluminium and Shadeed Iron &amp; 
Steel in Sohar. As a strategic policy we concentrate on the Muscat area, as our 
core strengths (resource support) are based here. We would like to go to far off 
location like Salalah, but preferably for large projects. There is difficulty in 
managing at that extra distance, for which we have to charge a premium, and that 
will make us less competitive compared to local contractors who can do the job 
with lower costs than us.” <br>
<br>
Business expansion beyond the Omani borders is ruled out for the company. 
Skinner admits that the company receives many queries for managing projects in 
other Gulf countries — but they are passed on as leads to Carillion plc in UK. 
In the GCC region, Carillion plc has a joint venture with Al-Futtaim in Dubai. 
However, the London-based headquarters is interested in spreading wings to other 
parts of the region — being currently under review.
