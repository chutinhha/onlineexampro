<p align="justify" dir="ltr" style="margin-left: 10px; margin-right: 10px">
<font face="Verdana" size="1">
<b>People focus</b><br>
Carillion Alawi meets the legal requirement of 27.5 per cent Omanisation level 
in its workforce. The rest of the team is comprised of expatriates drawn 
primarily from India. With a couple of big projects in hand, the company is in 
the process of increasing its team strength by 25 per cent. It even plans to 
take its Omanisation level to 30 per cent. Isn’t it a big challenge for the 
company? “Not really,” says Skinner. <br>
<br>
“Most of our work doesn’t require long-term training. Many a times the training 
session, spanning over 3-6 months, is sufficient to do the job — which makes it 
easy for us to hire more Omanis. The challenge is maintaining productivity 
standards. We did find a problem earlier trying to integrate Omani workers into 
the teams comprising mainly expatriates. That worked with a moderate degree of 
success. We could see that it wasn’t the ideal solution to our problem. What we 
have done now is to comprise purely Omani teams under the guidance of our key 
members. It has worked very well, as the productivity of Omanis has gone up 
substantially. As a policy, we want Omanis to have good quality productive jobs 
— and we are ready to provide them with whatever support it requires. In terms 
of hiring, we keep in touch with government bodies to hire local Omani talent. 
We like to invest back in the country. Omanisation is the key, not for surviving 
but for thriving,” he says. <br>
<b><br>
Challenges ahead</b><br>
With reputed projects such as Muscat City Centre expansion, Seeb Airport 
Interchange, Deputy Prime Minister’s Office and new Sultan Centre at Al-Amerat — 
which are expected to keep Carillion Alawi busy not just in 2006 but for most of 
next year as well. Skinner is not worried on the business generation front. But 
that doesn’t make him complacent — as he says, “Unlike other businesses which 
follow annual growth cycles, in the contracting business we, at Carillion Alawi, 
follow a three-year business plan. So in 2006, I plan in advance on the business 
opportunities till 2009. Besides executing the ongoing jobs and arranging 
resources to start the new jobs in hand, we are submitting bids for tenders open 
now — and keeping close watch on new projects such as The Wave which are 
expected to announce their fresh tenders in the next few months.” <br>
<br>
However, a key area of concern for Carillion Alawi is safety standards. “We are 
very actively promoting the highest safety standards, targeting zero accident. 
We see no reason why the safety standards in Oman should be less than the safety 
standards on the sites in the UK,” says Skinner. The key elements that underpin 
the company’s health and safety policy include awareness, competence, compliance 
and excellence. <br>
<br>
“What we want to do is encourage clients and contractors to come on this journey 
of health and safety improvement. We need to demonstrate where we are going on 
this journey, share with them some of the ideas we have and how they can help to 
support these ideas — and deliver the ultimate in zero accidents in health, 
safety and environment,” informs Charles Yates, group safety coordinator at 
Carillion Alawi.</font></p>
