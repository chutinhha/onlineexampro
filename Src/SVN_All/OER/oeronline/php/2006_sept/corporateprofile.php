<b>Deriving strength from the parents</b><br>
“We follow the same management system as in operation in Carillion plc UK. We do 
make certain alterations to suit the local market conditions and environment, 
but generally the company here is managed as it is done in UK. We have a similar 
kind of control, structures and corporate governance norms etc. As head of Oman 
operations, I keep in touch and discuss business management related matters with 
Alawi Enterprises’ top management as well as Carillion plc’s senior managers in 
Dubai and London,” informs Skinner.
