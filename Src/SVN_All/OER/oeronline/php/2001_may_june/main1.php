                  <td width="100%"><font face="Verdana" size="4" color="#FF0000">... but a green golf course needs water</font><p><font face="Verdana" size="1"><b>Despite the withdrawal of a consortium member and the shelving of plans for a public share float, the much-delayed Muscat Waste Water Project is finally set to go ahead. In an exclusive report, OER looks at developments in the $US600 million privatisation deal that, in addition to giving Muscat its first green golf course, will revolutionise the city's water system</b></font><font face="Verdana" size="2"><br>
                    <br>
                    <img border="0" src="../../images/2001_may_june/greengolf2.jpg" align="left" hspace="5" vspace="5" width="134" height="240"></font><font face="Verdana" size="2">Negotiations between the Government and a consortium of companies to finalise the $US600 million Muscat Waste Water Project have reached an advanced stage, and an agreement could be signed within weeks.&nbsp;<br>
                    Sources have told OER that settlement on the crucial issue of how much compensation the consortium should pay the Government for use of the existing pipeline network in Muscat is expected soon. It is understood both the parties have reached agreement on the water tariff, and are close to settling a number of other issues relating to commercial, legal and technical aspects of the project.&nbsp;<br>
                    If everything goes smoothly, a framework 32-year concession agreement for managing and creating the additional pipeline and sewerage infrastructure in Muscat would be signed by the two parties by the middle of May.&nbsp;<br>
                    <br>
 The concession pact is structured on a build-own-operate-transfer (BOOT) basis and the consortium will not only take over the existing pipeline network, but also add on additional pipelines, pumping stations, sewerage treatment plants and waste disposal systems.&nbsp;<br>
                    Among the Gulf states, this project is the first of its kind on such a large scale.&nbsp;<br>
                    <br>
                    However, with one of the leading consortium members withdrawing from the deal, and in light of sluggish capital market conditions, new investors are being sought.&nbsp;<br>
                    <br>
                    Consortium members say the decision of Covanta Energy (formerly Ogden Corporation) to withdraw from the project will not delay its implementation, as another founder shareholder has already been found. OER understands Dutch-English company Cascal has come on board.&nbsp;<br>
                    As the consortium has already spent money preparing the network design and master plan, the new investors will have to share the initial development cost. Therefore, the equity stake will be offered at a premium.&nbsp;<br>
                    Additional founder shareholders are also needed because of the sluggishness in the capital market. When the memorandum of understanding was signed in July 1997, the Government insisted that a 40 per cent stake in the company should be offered to the public by way of an initial public offering when the deal was finally struck.&nbsp;<br>
                    But with the Muscat Securities Market in the doldrums, it would be difficult to raise the required funding from the investing public and the promoters will now have to contribute the entire equity capital.&nbsp;<br>
                    <br>
                    <img border="0" src="../../images/2001_may_june/greengolf1.jpg" align="right" hspace="5" vspace="5" width="250" height="180">The consortium consists of Galfar Group, Bank Muscat, Auscon Consultants, HH Sayyid Kais bin Tarik Al Said - who negotiated an allocation of water for the green golf course project - and Yaqub al-Harthy, with Cascal completing the line-up.&nbsp;<br>
                    Envisaged to develop the water and sewerage water pipeline network in the entire Muscat capital area ranging from Qantab to Qurayyat and from Mabela to Muttrah, the project is estimated to cost around $600 million.&nbsp;<br>
                    Of the total project cost, almost $78 million will be raised by way of equity capital contributed by the promoters, while the rest will be term loans from financial institutions. The network plan was designed by Mott MacDonald and has taken into account present and future demographic needs.&nbsp;<br>
                    <br>
                    The entire project, which is expected to benefit 600,000 people in Muscat, will be implemented in two phases.<br>
                    <br>
                    The master plan has taken into account a compounded population growth of three per cent per annum, until the end of the 32nd year. The first phase, which will cover 50 per cent of the population, would take six to eight years to complete and the second phase will take another seven years. Income generated by the company over time is expected to partly finance the venture.&nbsp;<br>
                    Once the company becomes operational, it is mandatory for building owners to connect their sewerage and pipelines to the network, spelling the eventual demise of the water tankers that supply water to parts of Muscat.</font></p>
