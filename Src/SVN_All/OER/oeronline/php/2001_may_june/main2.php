                  <td width="100%"><font face="Verdana" size="4" color="#FF0000">Top
                    Guns</font><p><font face="Verdana" size="1"><b>Defence brings Oman and us together</b></font><br>
                    <font face="Verdana" size="2"><br>
                    </font><font face="Verdana" size="1"><b>Oman's biggest defence deal in a decade signals a sea-change in relations between the Sultanate and the United States, writes OER defence correspondent Michael Knights</b></font><font face="Verdana" size="2"><br>
                    <br>
                    <img border="0" src="../../images/2001_may_june/guns1.jpg" align="left" hspace="5" vspace="5" width="225" height="194">When His Majesty Sultan Qaboos bin Said announced  Oman's intention to purchase a squadron of US F-16 aircraft and 20 British Super Lynx helicopters in early March, it came as something of a surprise.<br>
                    Both Lockheed Martin, vendors for the F-16 sale, and Agusta Westland, producers of the Super Lynx, were reportedly caught off guard by the announcement, made by His Majesty to Royal Airforce of Oman officer cadets.&nbsp;<br>
                    Defence experts believe the reason that further details have not been revealed is largely because the agreements are still being negotiated.&nbsp;<br>
                    <br>
 But there is little doubt that Oman's biggest arms purchase in a decade will have a lasting impact on its relations with the United States. The F-16 deal marks the first time the US has won a major combat aircraft contract in the traditionally British and European marketplace.&nbsp;</font></p>
                    <p><font face="Verdana" size="2"><img border="0" src="../../images/2001_may_june/guns2.jpg" align="left" hspace="5" vspace="5" width="223" height="138">When the details of the deal are agreed, Omani-US economic ties are likely to deepen. Though offset arrangements are more common in major arms deals such as the UAE's $US8 billion F-16 and munitions purchase, Oman may well capture large amounts of US investment in her non-oil industries as part of future F-16 buys. It might also give Oman the opportunity to boost its manufacturing sector in the future by negotiating a licensed production deal for more F-16 purchases in the same way that other buyers, such as South Korea, have done. This could benefit Oman's manufacturing sector by allowing components to be built at factories in the Sultanate.<br>
                    In the meantime, Oman will greatly improve its interoperability with the US, allowing it to exercise more effectively with US air forces as Egypt, Saudi Arabia and Bahrain can.&nbsp;<br>
                    <br>
                    The F-16 deal opens the door to Oman for US defence contractors, particularly Lockheed Martin, which has already dealt with the Sultanate over the three C-130 transport aircraft it operates.&nbsp;<br>
                    A source at Lockheed Martin says major arms sales, particularly of F-16s, usually expand the relationship between the US and the purchasing country.&nbsp;<br>
                    "F-16 sales are usually influential because of their high profile," the source says. "They are highly visible, have a high dollar value, represent technology, are cornerstones of national defence and are a symbol of national pride.&nbsp;<br>
                    "In many countries, F-16 sales are also important because of the industrial participation that accompanies the sale."&nbsp;<br>
                    Jim Dorschner, defence analyst at the International Centre for Security Analysis, says the deal is important to US-Omani relations in two ways. It allows the US to repay Oman for giving it basing rights in the Sultanate, and it is a way for the US to get "a foot in the door" of the Omani market.&nbsp;<br>
                    As Oman becomes familiar with the F-16, future sales of the aircraft - perhaps including 'new build' models with long lifespans - are more likely.&nbsp;<br>
                    Lucrative upgrade deals will favour the US (though Singapore also offers these services) and the US will press Oman to follow up its aircraft purchases with large armaments packages.&nbsp;<br>
                    <br>
                    While no numbers have been released, the costs of procuring both F-16s and Super Lynxes are well documented. New Block 40 or Block 50 model F-16s, such as those operated by Bahrain and Egypt, cost around $25 million to 30 million, and the UAE's advanced Block 60 models will cost around $50 million each.&nbsp;<br>
                    Oman initially looked at the option of buying surplus Block 10 and Block 15 F-16s, which it could potentially get for as little as $8 million each and then upgrade.<br>
                    Nigel Vinson of the London-based Royal United Services Institute told OER: "The price of these aircraft depends on the country, its profile and what the US wants to get out of the deal."<br>
                    In some cases the US has given these aircraft for free, charging only delivery costs, to gain access to new markets.&nbsp;<br>
                    But the Sultanate is now understood to be considering the more expensive models and, depending on which model best suits its needs, may choose between Block 40, Block 50 or Block 60 F-16s.<br>
                    Sources have suggested that between 12 to 16 aircraft are to be bought, with deliveries beginning within 18 to 20 months.&nbsp;</font></p>
                    <p><font face="Verdana" size="2"><img border="0" src="../../images/2001_may_june/guns3.jpg" align="right" hspace="5" vspace="5" width="250" height="183"> Similarly, Oman's purchase of 20 Super Lynx helicopters from Britain will enhance its already strong relationship with the United Kingdom. With the cutting-edge Super Lynxes going for $32 million each, the Agusta Westland deal could cost up to $640 million.&nbsp;<br>
                    <br>
                    But industry sources suggest the price tag could eventually be as low as $7.5 million to $8 million per helicopter, depending on how the deal is structured.&nbsp;<br>
                    <br>
                    The Super Lynx purchase is a true long-term investment and the first of a series of helicopter deals required in the near future.&nbsp;<br>
                    Oman has long expressed a desire to enhance its helicopter fleet. The Government recently announced that up to 50 new helicopters would be purchased by 2003.&nbsp;<br>
                    The highly advanced Super Lynx will fulfil a number of Omani requirements well into the 21st century. Primary among these, according to the International Institute for Strategic Studies' naval analyst Joanna Kidd, will be maritime search and rescue, reconnaissance and attack missions, carried out both from shore bases and from Oman's two British-made Qahir-class Corvettes and amphibious assault ships.<br>
                    Though observers registered surprise at the lack of warning concerning Oman's largest arms deals in over a decade, the decision to boost the Sultanate's air and maritime defence capabilities has been a long time coming.&nbsp;<br>
                    In the late 1980s Oman shelved its ambitious plans to purchase a squadron of BAE Tornados and instead upgraded its fleet of SEPECAT Jaguar strike aircraft and purchased less expensive air defence and training aircraft like the BAE Hawk 203. While other GCC states splashed out following the 1991 Gulf War, Oman focused on maintaining its 40 aircraft fleet.&nbsp;<br>
                    But with $2.4 billion budgeted for defence spending in fiscal 2001, the recently announced deals are likely to be the first of many.&nbsp;</font></p>
                    <p align="center"><font face="Verdana" size="2"><img border="0" src="../../images/2001_may_june/guns4.jpg" width="250" height="150"></font></p>
                    <p><font face="Verdana" size="2"><b>Swift Sword</b><br>
                    <br>
                    Omani-Anglo wargames to be held in the Sultanate this autumn will be a major event on both the military and political agendas. The Saif Sareea or "Operation Swift Sword" exercises - due to take place between September and November, will represent the largest UK deployment since the Suez crisis in the 1950s. Over 24,000 British troops will participate, compared with the previous largest force of around 5,000.&nbsp;<br>
                    Essentially aimed at boosting sagging UK arms sales in the Gulf and at bolstering political ties with the GCC, the games will also serve as a vivid illustration of the special relationship that exists between Oman and Britain. The size of the exercise also indicates Britain's desire to cement its military relationship with Oman well into the 21st century. Though the Super Lynx purchase shows the strength of the relationship, it may increasingly face challenges as a result of Oman's F-16 deal with the United States.&nbsp;<br>
                    <br>
                    <b>GCC Defence Spend&nbsp;</b><br>
                    <br>
                    Though Oman's 1999 defence expenditure of $1.6 billion was relatively modest in GCC terms, with only Qatar and Bahrain spending less, it was large for the size of its economy. Aside from Saudi Arabia and Qatar, Oman spends the largest proportion of GDP (10.9%) on defence in the GCC. This compares with 2.6% in the UK, 3.2% in the US and 6.2% in Iran. While Omani defence spending has been prudently restrained throughout the 1990s, declining by 9.0% in 1999 against a GCC average of a 0.8% rise, this trend looks set to change in the 2001-2005 Five Year Plan. The $2.4 billion allocated to defence in 2001 alone is a massive increase on the $1.6 billion allocated in 1999 and the $1.75 billion allocated in 2000. Oman typically overspends its defence budget by 5.0%-18% and if this trend continues the Sultanate could see its highest defence spending for over 15 years.&nbsp;</font></p>
