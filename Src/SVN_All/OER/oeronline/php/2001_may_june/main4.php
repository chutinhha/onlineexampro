                  <td width="100%"><font face="Verdana" size="4" color="#FF0000">To Oman's Credit</font><p><font face="Verdana" size="1"><b>Standard and Poor's has awarded Oman its first ever credit rating upgrade and pointed to a robust fiscal future for the Sultanate. But the international ratings agency also had some words of warning. OER Editor Matthew Brockett finds out why</b></font><br>
                    <font face="Verdana" size="2">
                    <br>
                    <img border="0" src="../../images/2001_may_june/oman1.jpg" align="left" hspace="5" vspace="5" width="250" height="187">
                    A credit rating upgrade might not sound glamourous, but it sends an important signal to international markets that a country is moving in the right direction.<br>
                    The more creditworthy a nation is, the safer others feel about doing business with it. A higher credit rating may also reduce the borrowing costs for a government and local corporations in international financial markets.<br>
                    On February 27, Standard and Poor's upgraded Oman's sovereign credit rating from BBB-minus to BBB, the first upgrade since the Sultanate was given a rating in February 1996.<br>
                    The powerful ratings agency said it believed the Omani Government would continue to pursue prudent fiscal policies, as outlined in the 2001-2005 Five Year Plan and the 2001 budget. It also said it expected the Government to post strong surpluses this year and next, rather than the deficits forecast in the budget.<br>
                    <br>
                    Why is Oman's fiscal future looking so bright?&nbsp;<br>
                    Standard and Poor's analyst Ashok Bhatia told OER from London that, under the guidance of His Majestry Sultan Qaboos bin Said, Oman's economic transformation had been astonishing. "Enormous strides have been made," he said.<br>
                    <br>
                    "If one looks at Oman's employment track record, it's impressive, as indeed is the economic development track record generally."&nbsp;<br>
                    The Government was prudent, he said, to assume an average oil price of $US18 per barrel, on which it based its forecast of an annual average budget deficit of 2.5 per cent of gross domestic product (GDP).<br>
                    But S&amp;P believes oil prices could be higher than that over the next couple of years, which would push the consolidated budget balance into the black. Rather than posting budget deficits, S&amp;P expects Oman to post surpluses of 5.0 to 6.0 per cent of GDP this year and 4.0 per cent of GDP in 2002.<br>
                    These surpluses would bolster the Government's reserve funds, while debt levels would remain broadly unchanged, Bhatia said.&nbsp;<br>
                    "Consequently, the Government's net asset position could increase significantly to about 40 per cent of GDP by year-end 2002 (from 29 per cent of GDP today)."<br>
                    Such an improvement in the Sultanate's net asset position would bode well for another credit rating upgrade. The Government's privatisation programme is also viewed positively by the ratings agency, as it will bolster the Government's accounts.<br>
                    Bhatia said the Government was doing a good job of using the budget to stabilise its economy - spending wisely when oil prices were low to insulate the non-oil sector, and putting money aside when oil prices were high to prepare for the next oil-price downturn.&nbsp;<br>
                    "That is exactly the point of our upgrade," he said.<br>
                    "Oman is essentially a two-sector economy, as indeed are all the other Gulf oil producers. There's the hydrocarbons economy, which loosely speaking is the revenue generator. Government budgetary expenditure is the key driver of non-hydrocarbon activity. The buffer between the two is the budget, and the budget has a strong stabilisation role. Our ratings hinge on subtle questions of degree."&nbsp;<br>
                    <br>
 But S&amp;P has identified several issues that could constrain Oman's economic growth and credit rating in coming years unless the Sultanate moves to address them.&nbsp;<br>
                    Other than the ever-present risk that oil prices could fall, Bhatia said growing pressure on Oman's labour market could hamper its economic progress.&nbsp;</font></p>
                    <p><img border="0" src="../../images/2001_may_june/oman2.jpg" align="right" hspace="5" vspace="5" width="224" height="191"><font face="Verdana" size="2">"We have a rapid rate of growth of the indigenous Omani population, and that has been absorbed to date by gradually reducing the expatriate population, such that the overall population growth rate of Oman has been tempered," he said.&nbsp;<br>
                    "There is a feeling at S&amp;P that there are limits to that process.<br>
                    "The expatriates are fulfilling vital roles and their numbers cannot be reduced beyond a certain point. And when that point arrives, what we'll see is a sharp increase in the overall population growth rate."<br>
                    Another issue was an apparent skills shortage in the labour market. Bhatia said the private sector in Oman often complained of a mismatch between the skills it sought and the skills available. Some labour market regulations, such as minimum wage requirements, also discouraged the private sector from absorbing adequate numbers of Omani job seekers, he said.&nbsp;<br>
                    In other words, if Oman wants another credit rating upgrade, it will have to address emerging labour market issues with the same vigour it has shown towards economic reform.&nbsp;</font></p>
