                  <td width="100%"><font face="Verdana" size="4" color="#FF0000">Hep B Breakthrough</font><p><font face="Verdana" size="1"><b>Omani finance and entrepreneurial spirit are behind a ground-breaking vaccine that could save millions of lives. Lakshmi Kothaneth reports&nbsp;</b></font><font face="Verdana" size="2"><br>
                    <br>
                    <img border="0" src="../../images/2001_may_june/hep2.jpg" align="left" hspace="5" vspace="5" width="225" height="177">Vaccination against the deadly hepatitis B virus is an expensive treatment, putting it out of reach for millions of the world's most vulnerable people. But a breakthrough in the fight against one of the world's worst diseases has been achieved by an Omani-Indian joint venture.<br>
                    <br>
 Under the chairmanship of His Excellency Yusuf Bin Alawi Abdullah, Oman's Minister responsible for Foreign Affairs, Hyderabad-based Shantha Biotechnics Ltd has developed a genetically-engineered vaccine at a fraction of the cost of other hepatitis B vaccines.&nbsp;<br>
                    Called Shanvac-B, the vaccine is available for just $US2 a dose - a far cry from the $8 to $10 cost of other vaccines currently on the market.&nbsp;</font></p>
                    <p><font face="Verdana" size="2"><img border="0" src="../../images/2001_may_june/hep1.jpg" hspace="5" vspace="5" align="left" width="117" height="175">Shantha Managing Director Varaprasad Reddy says Shanvac-B is making a huge difference in developing countries like India, Bangladesh, Sri Lanka, Nepal and the Philippines.&nbsp;<br>
                    <br>
                    "It is a life-saving vaccine," Reddy told OER.&nbsp;<br>
                    "Around 350 million people all over the world are affected by this viral infection, hepatitis B. There is no cure, only prevention. No doubt there are multinationals who have done this job, but at a very high cost. Only the rich can afford it; the common man has to go without.&nbsp;<br>
                    "We thought we must do some service in this area. Through our indigenous research and development efforts, we could bring out the vaccine at a very low cost and, at the same time, four times more powerful than existing vaccines."<br>
                    With help from Oman, Shantha was the first company to produce a genetically-engineered hepatitis B vaccine in India, propelling it to the forefront of India's biotechnology industry.&nbsp;<br>
                    It all started in 1994, when Alawi and Reddy co-founded the Omani-Indian joint venture with a mission to research and develop genetically-engineered human health care products that were affordable to all.<br>
                    Alawi says a vaccine for hepatitis B was chosen as a starting point because of the widespread destruction the virus was wreaking on the world. It struck him as strange, he says, that a disease more deadly than AIDS had a vaccine that was not cheap enough to reach the people who needed it most.<br>
                    "We saw it grow from a study in a lab to what it is today," recollects His Excellency in an exclusive interview with OER.<br>
                    "I do know a number of Omanis, who I am close to, who suffer from hepatitis. So I know a lot about how severe this disease is and the way it should be treated. When this opportunity came along I said 'it's worth it'. We did it and we are a success."<br>
                    But success did not come easily. It took over five years of hard work and in-house research by a dedicated team of scientists and technicians to achieve the goal.&nbsp;<br>
                    "It is not due to any one person alone. Everyone from the lab technician support staff to the management team and the investors has been emotionally tied to the project," Alawi says.<br>
                    Today the Oman-India partnership is the most successful joint venture between the two countries in the arena of high technology health care.<br>
                    International pharmaceuticals giant Pfizer is marketing Shanvac-B in India and overseas under the brand-name Hepashield.  Shantha has achieved sales of 19 million doses to date. Currently, Shanvac-B has a 46 per cent share of India's total domestic market for hepatitis B vaccines. The vaccine's installed production capacity of five million doses has been increased to 40 million doses.<br>
                    Shantha started with a modest capital of $380,000 and is now a company with a net worth of $18.12 million and a market capitalisation of $192.71 million.&nbsp;<br>
                    <img border="0" src="../../images/2001_may_june/hep3.jpg" align="right" hspace="5" vspace="5" width="200" height="200">In its first year of commercial operations the company achieved a turnover of $1.51 million. In the 1999-2000 fnancial year its turnover was $6.82 million, and it made an after-tax profit of $2.72 million. During the current financial year Shantha has recorded an increase in sales of 20 per cent. If this rate of growth continues, the company estimates that by 2005, it will have sales of $85 million and an after-tax profit of $19 million.&nbsp;<br>
                    Shanvac-B belongs to the "recombinant" vaccine group, meaning it is created by recombining genetic material. It is prepared from genetically engineered yeast cells.<br>
                    The first hepatitis B vaccines were derived from blood plasma. This raised fears of blood-borne illnesses being transmitted through the plasma of blood donors. A study on recombinant vaccines proved they produce large amounts of antigens (which stimulate the production of antibodies) in a shorter production cycle. The technique is not dependent on a donor pool either. Recombinant vaccines have been replacing plasma-derived ones worldwide for some time now.&nbsp;<br>
                    Reddy says if it was not for the patents that big drug companies were able to claim on pharmaceuticals, low-cost vaccines like Shanvac-B could reach across the globe.&nbsp;<br>
                    "Unfortunately, with the kind of system we have - the patent rights - we are not able to penetrate through all the markets," he says.<br>
                    "At least we could do a lot of service to India, Bangladesh, Sri Lanka and other Asian countries. We are not able to do that in America and Western Europe. On the other hand, they are rich and they do not need this vaccine."<br>
                    <br>
 Shantha is working on multiple vaccines and the development of other 'biologicals' - drugs that are produced by living organisms - whose patents are about to run out.&nbsp;</font></p>
                    <p><font face="Verdana" size="2"><img border="0" src="../../images/2001_may_june/hep4.jpg" align="left" hspace="5" vspace="5" width="250" height="157">If they manage to keep the cost of these biopharmaceuticals as competitive as Shanvac-B, millions of lives could be saved.<br>
                    Reddy says though the Indian side of the partnership provided the technology, the company might never have succeeded without the Omani financial backing and entrepreneurial spirit.&nbsp;<br>
                    "His Excellency Yusuf Bin Alawi Abdullah as chairman of the company has been providing mature and far-sighted direction with like-minded entrepreneurs and partners," he says.<br>
                    Various institutions supported and funded Shantha's research program. From the Sultanate it was Oman International Bank, and from India it was the Ministry of Science and Technology Development Board. The World Bank also contributed to the research.<br>
                    His Excellency says the company hopes to train Omanis in its R&amp;D labs, and distribute Shanvac-B throughout the Gulf.<br>
                    "We in Oman are a young country, but a country with a drive to succeed," he says. "Our youth are coming up with new ideas. If I can share and expose them to some areas of new technology, make them aware of the new horizons in technology and the possibility of commercial success, it would be a small mission accomplished."<br>
                    But more than that, he believes Shantha's low-cost vaccines will put pressure on other companies to lower their prices, making life-saving drugs available to more people.&nbsp;<br>
                    "This is a venture that will be great in the future when there will be a break through in other medications, other vaccines and multiple vaccines, which the company is working on right now," he says.&nbsp;<br>
                    "So many, many people can benefit from this."<br>
                    <br>
                    <b>Hepatitis B kills</b><br>
                    The World Health Organisation (WHO) says hepatitis B is one of the world's most serious diseases and is a major public health problem globally. Currently there is no effective treatment for hepatitis B - a disease more infectious than AIDS. Prevention is considered the most effective way to control its spread, with vaccination the most effective form of prevention.&nbsp;<br>
                    In Oman, children are immunized free by the Government, but this is a luxury not enjoyed in many countries. Of the two billion people who have been infected with the hepatitis B virus (HBV), more than 350 million have chronic (life long) infections. These people are at high risk of death from cirrhosis of the liver and liver cancer, diseases that kill about one million people each year. Although vaccines will not cure chronic hepatitis, they are 95 per cent effective in preventing chronic infections from developing.<br>
                    In 1991, WHO called for all children to receive hepatitis B vaccinations, and 116 countries have added an HBV vaccine to their routine immunization programmes. However, children in the poorest countries, who need vaccination the most, have not been receiving it because their governments cannot afford it.&nbsp;</font></p>
                    <p><font face="Verdana" size="2"><b>What is hepatitis?</b><br>
                    Hepatitis means inflammation of the liver, and the most common cause is infection with one of five viruses, called hepatitis A,B,C,D, and E. All of these viruses can cause an acute disease with symptoms including yellowing of the skin and eyes, dark urine, extreme fatigue, nausea, vomiting and abdominal pain. It can take several months to a year to feel fit again.&nbsp;<br>
                    <br>
                    <b>Who gets hepatitis B?</b><br>
                    In much of the developing world, (sub-Saharan Africa, most of Asia, and the Pacific), most people become infected with HBV during childhood, and eight to 10 per cent of people in the general population become chronically infected.&nbsp;<br>
                    High rates of infection are also found in the Amazon and the southern parts of Eastern and Central Europe. In the Middle East and Indian sub-continent, about five per cent are chronically infected. Infection is less common in Western Europe and North America, where less than one per cent are chronically infected.<br>
                    <br>
                    <b>How do people get infected ?</b><br>
                    Hepatitis B is transmitted by contact with blood or body fluids of an infected person in the same way as human immunodeficiency virus (HIV), the virus that causes AIDS. However, HBV is 50 to 100 times more infectious than HIV</font></p>
