<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
</font><b><font face="Verdana" size="4">Leading with an
Open Mind</font></b><font face="Verdana" size="1"><br>
<b>One of the defining responsibilities of today’s leadership is to attract the 
best ideas from the most people, and open source software provides a unique 
opportunity to do just that, writes William C. Taylor<br>
</b>
<br>
<img border="0" src="mana1.jpg" width="150" height="226" align="left" hspace="6" vspace="4">Nothing is more powerful in business than a truly original idea–a distinctive 
point of view that redefines an industry, a breakthrough design that transforms 
a product category. In an era of hyper-competition and non-stop innovation, the 
one sustainable form of market leadership is thought leadership–generating 
better ideas and making smarter adjustments than the competition. Today, one of 
the defining responsibilities of leadership is to attract the best ideas from 
the most people–to master a world in which “nobody is as smart as everybody.”<br>
<br>
The rise of open-source software in general, and the Linux operating system in 
particular, may be the best-known case study of this new grassroots approach to 
innovation. Linux runs on computer code written by programmers scattered all 
over the world, people who are passionate enough about their craft, or who have 
a strong enough stake in the success of the project on which they work, to 
volunteer their time and expertise to write software that no company owns but 
millions of people can’t live (or at least work) without. Linux and open-source 
software are about more than the future of computer systems. They are a metaphor 
for the future of innovation itself. The world is teeming with smart, skilled, 
passionate people who are eager to demonstrate how much they know and how good 
they are–people who just might contribute a killer idea, or a whole collection 
of small innovations, that set you apart from the competition.<br>
<br>
Consider the experience of John Fluevog, the world-renowned shoe designer, who 
opened his mind to the power of open-source innovation. Over the last few years, 
the company’s founder and “sole proprietor” hasn’t just been presenting ideas 
about shoes and style to customers; he has also been soliciting ideas from them. 
He posts the submissions on his company’s website, invites visitors to vote for 
their favourites and manufactures and sells the most promising designs. He 
actually calls it all “open-source footwear.”<br>
<br>
Applying the outside-in mind-set of open source means abandoning familiar 
assumptions about where great ideas come from, who gets to be part of your 
company and how to inspire the best contributions from them.<br>
<br>
1. Keep the focus narrow and tightly defined. There’s a big difference between 
tapping outside brainpower and engaging in the kind of free-form brainstorming 
that often feels vague and unproductive. The most effective open-source leaders 
understand that collaboration works best when the focus of the collaboration is 
narrow and tightly defined: fix a bug in Linux, design a new shoe, solve a 
problem in the lab.<br>
<br>
2. Keep broadening the participants. The real magic of open-source innovation is 
not just that lots of people will offer ideas, but also that lots of different 
kinds of people will offer ideas. Don’t limit the circle of participants to 
specialists in your home country, your industry or your product categories.<br>
<br>
3. Don’t keep all the benefits to yourself. If you expect strangers to share 
their best ideas with you, then don’t be surprised when they expect something in 
return. It can be money, it can be recognition, but more often than not, what 
draws people into open-source projects is the chance to push themselves, to 
develop their skills, to interact with the best people in their field. Which 
means you’ve got to create an organisation (and a style of leadership) that is 
as comfortable sharing ideas with the outside world as it is accepting ideas 
from the outside world.<br>
<br>
Darren Carroll has spent years working to realise the promise of open-source 
innovation. A veteran executive of Eli Lilly, Carroll was the pharmaceutical 
giant’s lead US attorney for Prozac before he became co-founder and chairman of 
InnoCentive, a “virtual R&amp;D lab” financed by Lilly and based in Boston.<br>
<br>
InnoCentive has signed up more than 30 “seeker” organisations from among the 
largest companies on earth. They include Lilly and P&amp;G, of course, along with 
Boeing, Dow, DuPont and NestlePurina. These are huge enterprises that together 
generate hundreds of billions of dollars in sales, invest billions in R&amp;D and 
employ tens of thousands of scientists and engineers. Yet they all have 
laboratories overflowing with unsolved problems that their researchers don’t 
have time to work on or don’t have the in-house expertise to solve.<br>
<br>
InnoCentive has registered more than 90,000 biologists, chemists, biochemists, 
materials scientists and other highly skilled professionals. It’s up to 
InnoCentive to connect its seekers with its solvers. Seeker companies post 
innovation “challenges” on the InnoCentive website.<br>
<br>
The challenges come with a scientific description, a deadline and an award, 
which can run as high as $100,000. (Postings don’t reveal the name of the seeker 
company, and they’re scrubbed clean of details that might hint at the product to 
which they’re connected.) Solvers compete to meet the challenge in a way the 
seeker company deems a success, at which point the solver gets paid and the 
seeker owns the intellectual property.<br>
<br>
For executives to unearth innovations, Carroll says, they must reinvent their 
style of leadership–stop thinking of themselves as problem solvers and start 
thinking of themselves as solution finders. “That’s not a semantic distinction,” 
he explains. “It’s one of the most fundamental distinctions we make. A problem 
solver is someone who gets handed a challenge and doesn’t rest until he or she 
has solved it personally. A solution finder looks around the world and is 
agnostic as to where the answer comes from, so long as it’s the best answer at 
the lowest cost in the shortest time.”<br>
<br>
<i>(William C. Taylor is co-author of a new book, Mavericks at Work: Why the Most 
Original Minds in Business Win, (William Morrow, October 2006). He is also a 
co-founder and founding editor of Fast Company magazine.)<br>
