<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
</font><b><font face="Verdana" size="4">Designed to thrill</font></b><font face="Verdana" size="1"><br>
<b>Contemporary design and cutting edge technology make the all-new 2007 Infinity 
M45 a luxury sedan to watch out for, says Anne Kurian </b> <br>
<br>
<b><img border="0" src="car1.jpg" align="left" hspace="6" vspace="4">The all-new 2007 Infinity M45 is styled under the revolutionary philosophy of 
Vibrant Design by Infinity, which used brush strokes to convey emotion and 
intensity. <br>
</b>
<br>
Speed thrills. So do power and technology. Add luxury to the aforementioned and 
what you have is an unbeatable combination on wheels. Or the all-new 2007 
Infinity M45. Belonging to the category of Infinity range of radical models for 
2007, the M45 is as impressive as the rest of the range. Included in the all-new 
range that Infinity has lined up for 2007 are G35 Sedan, G35 Coupe, FX35 and 
QX56. Besides revving up the features of each of its models, Infinity has 
infused the vibrant design philosophy in the 2007 models. So along with the 
awe-inspiring features of the Infinity automobiles what also thrills you are the 
snazzy exteriors. <br>
<br>
The M45 might be the youngest of the Infinity range but does not fail to score 
in any of the priority categories, be it design, performance or technology. In 
fact, this 362-hp, 4.5-litre V8 sedan can effortlessly give its competitors a 
run for their money. A short spin in the M45 proved that this luxury vehicle 
could soon become the successful urban professional’s choice!<br>
<br>
<b>Style factor<br>
</b>Simple yet elegant lines define the body of the M45. Much like an artist’s 
impression, the M45 bears the unmistakable characteristics of a sedan that holds 
many promises. Design is one of the key features of the all-new models of 2007 
and the M45 is surely one of the best examples. Without going over the top the 
designers at Infinity has managed to strike a fine balance between form and 
style. <br>
<br>
The interior of the car is equally appealing with an impressively laid out 
dashboard that has been inspired by a piano keyboard. The wood and leather 
accents, the fuss-free interior design and the acute detailing given to design 
are some of the captivating features of the luxury sedan.<br>
<br>
<b>Technology notes<br>
<img border="0" src="car2.jpg" width="200" height="201" align="right" hspace="6" vspace="4"></b>A recent study by automobile experts showed that the interior of a car is as 
important, if not more, as the exterior design. The more importance technology 
gets the better, as it exhilarates the driving experience. The Infinity 
engineers definitely have given this much thought and introduced what would in 
automobile jargon be known as intuitive technology. For example, the intelligent 
key does not require you to use it manually to open the doors. Just have it in 
your pocket or purse and you enter and drive with absolutely no effort. <br>
<br>
The seat and steering wheel can be set to two drivers’ requirements; the 
driver’s seat, in fact, has a 10-way power control. Infinity pampered us 
completely! After fiddling with the seat knobs, we turned our attention to the 
Infinity Controller. The multi-function control knob let us adjust everything 
from temperature to audio and also check the car’s technical details like tire 
pressure, petrol gauge, oil filter, et al. All these details and more were 
displayed on the fairly large screen placed on the dashboard. Incidentally, this 
screen also doubles as the rearview screen which shows what’s directly behind 
the car when you reverse. <br>
<br>
And what’s an Infinity without earth-shattering music? The resounding Bose 
system in the M45 had us swaying to Eagles and Jon Bon Jovi, our personal 
favourites on the road. <br>
<br>
<b>Driving Impressions<br>
</b>Zero to 100 in 5.6 seconds. Now that’s no mean feat, especially when the M45 
comes with an automatic transmission that also features a manual shift mode 
gear. The oft-heard complaint that automatic transmission cars fall behind their 
manual gear counterparts would not include the M45. Responsive, swift and 
precise, the M45 does not hesitate even for a fraction of a second when you step 
on the accelerator. And at curves, the rear active steer technology gets all the 
four wheels to move in unison. It’s hard not to be impressed with the power of 
the M45.<br>
<br>
Not satisfied with the run on the straight asphalt, we took the M45 for an 
uphill climb in Qantab and the sedan did not disappoint. Without even a murmur, 
the M45 zipped up the road and ground to a perfect halt when we jammed the 
brakes. We were now thoroughly impressed. This beauty was a performer and 
reliable. <br>
<br>
To say that the M45 moves effortlessly might be a cliché, but then that’s the 
truth. Whether it is through the trying traffic or during a steep climb, the 
M45’s V8 engine packs in quite a punch, which made our experience in the sedan 
memorable.<br>
<br>
<b>Safety Features<br>
</b>As with all its vehicles, the Infinity Safety Shield ensures that the M45 
compiles to all advanced safety technologies. So along with the brake assist and 
electronic brake force distribution, the M45 also features the Vehicle Dynamic 
control technology, which ensures you never go out of control even while 
cruising at high speeds. Also included in the M45 are side-impact and front-rear 
roof-mounted curtain airbags. </font>
