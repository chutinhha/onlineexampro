<b>Performance of other GCC markets</b><br>
Saudi Arabia continued with the upward trend to gain by 9.83% while Abu Dhabi 
and Kuwait increased by 4.99% and 2.25% respectively. However, Qatar continued 
on the back of selling pressure to decline by a 3.25%. Bahrain and Dubai also 
witnessed substantial losses to decline by 2.18% and 7.03%, respectively. 
However, on an YTD basis Qatar has witnessed a huge decline of 13.30% while 
Saudi Arabia made gains of 9.83%. <br>
<br>
<b>UAE: The real estate sector </b> <br>
The Real Estate sector was buoyed by the increasing investment in 
infrastructure, due to the country being positioned as an attractive tourist 
destination in addition to the increase in the residential and non-residential 
units. With the construction sector, both sectors cumulatively accounted for 
14.6% of GDP in 2005. The sector’s key drivers include, among others, growing 
population especially due to expatriates’ influx, ample liquidity, and friendly 
regulatory environment. The influx of expatriates is a major factor that drives 
housing demand. Assuming a 7.75% growth of the expatriate population, which is 
higher than that of the local population, an estimated inflow of around 292,000 
expatriates in 2007 is expected.<br>
<br>
