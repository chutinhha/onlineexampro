</font><font size="4" face="Verdana"><b>Networking on the greens</b></font><font face="Verdana" size="1"><br>
<b>Mudit Saxena, Head of Corporate Banking at Bank Dhofar, takes his golf as 
seriously as his work - and is naturally happy when the two overlap. Uma Devi 
Jadhav finds out more<br>
</b>
<br>
<img border="0" src="passtime.jpg" width="200" height="223" align="left" hspace="6" vspace="4">Starting with India’s advertising giant Lintas and then carving out a niche for 
himself in banking with American Express and HDFC before moving on to Emirates 
Bank in Dubai, the career shifts were a natural progression for Mudit Saxena, 
currently Head of Corporate Banking at Bank Dhofar. The same goes for his 
leisure time activity, where he has moved on from soccer to golf. Two different 
jobs, two different games, two different countries– “its just how you apply 
yourself and connect that matters!” <br>
<br>
“Basically, I am a very active person and need to walk or swim everyday in case 
I am not playing a game. I used to play soccer for Mohun Bagan’s junior team in 
Kolkata, India. Even in those days my friends used to ask me to play golf with 
them, but I thought it was a game for the oldies! But when I was working in 
Mumbai, I just took it up and now I wish I had started even earlier,” says Mudit. 
“It really does seem a natural switch, as it is so difficult to play team games 
after one starts working.”<br>
<br>
“I started playing serious golf only in 2002 in Dubai. In India, though it is 
cheaper to play and learn golf, it is difficult in terms of time and commuting, 
especially in the metropolitan cities. In Dubai, I found golf is a very serious 
part of work and indeed corporate life. <br>
<br>
Since my work involved meeting clients, it was most convenient for me to simply 
tee off with them at the slightest opportunity!” The inevitable switch from 
grass courses in India to sand courses in Dubai had to be done but the young 
banker feels “I find it easier here on sand though and my handicap has come down 
from 18 to 14. I won the best net in the BEC tournament and was the first runner 
up in the Oman National in the net tournament too,” he says, very satisfied with 
his game.<br>
<br>
Today, however, he has to balance his time with his work, children, wife and 
parents. And, “now, I only get to play on weekends and that too only on one 
day,” he moans. “It’s two days only if there is a tournament. Very rarely, 
during the peak season do I go to the driving range for practice. There is no 
academy here for coaching young children, so I need to take my children to play 
tennis or for a swim.<br>
<br>
I do wish the clubs here had a provision for children, then on weekends one can 
enjoy an outing and also play with one’s family members.” Notwithstanding this, 
he is happy to indulge in his favourite game whenever he gets the time. “It’s a 
great game to stay mentally alert and physically healthy. Some of the courses 
are so beautifully designed that they help you connect with nature. Every year, 
some of us friends take a short holiday to play golf and come back refreshed. It 
is really a game like wine, which matures with age, and one can pursue the game 
all through one’s lifetime,” he concludes. </font><p align="right" dir="ltr" style="margin-left: 10px; margin-right: 10px">
