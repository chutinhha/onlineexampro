<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
The port’s management rests with the Salalah Port Services Company SAOG, with 
the single largest holding of shares (30 per cent) lying with foreign owner A.P. 
Moller-Maersk, and 70 per cent owned by Omani companies, investors and 
government collectively. SPS has signed a management agreement with APM 
Terminals, under which the Dutch company operates the port with best practices 
learned from the more than 45 container ports it is involved in globally. The 
largest in the Sultanate, it is a deepwater port and can accommodate large 
vessels up to 16m draft. The government and the Salalah Port Services are making 
a combined investment of US$356 million to increase the port’s capacity to four 
million TEU per annum from the present 2.4 million TEU. The developments also 
include dredging the port to 18m water depth.<br>
<br>
After just a few years since its creation, the Port of Salalah, 18 km south of 
Salalah city, has developed into one of the world’s largest container terminals, 
so much so that today it is recognised as one of the world’s leading 
transhipment centres, catapulting Oman onto the global shipping map. It is 
counted among the handful of ports in the world capable of handling the largest 
container vessels and is even equipped to handle the next generation of vessels, 
such as Maersk Line’s so-called E-Class vessels that are 397 meters long and 56 
meters wide! According to Tiemen Meester, CEO of the Port of Salalah, the port 
falls in the genre of common-user multi-purpose, and is the main container 
transhipment terminal of the region. It also has facilities to handle bulk 
cargo, general and liquid cargoes, and it offers value-added services such as 
bunkering, container repairs, a container freight station, warehousing and ship 
repairs. The port currently has four berths along 1,236 metres of quay and the 
capacity of the container terminal stands at 2.5 million TEU per annum. The 
government and SPS are investing US$356 million to increase the handling 
capacity of the port up to 4.4 million TEU per annum with the construction of 
two new berths with 10 very large gantry cranes. The first berth, which is 520 
metre long and equipped with six of the world’s largest gantry cranes, will have 
18m water depth and will be inaugurated in April. The construction is handled by 
a joint venture between CCC and Hani Archirodon that has won a contract worth 
US$262 million to construct two additional berths. <br>
<br>
The strategic location of the port, at the centre of the Indian Ocean Rim of 
countries (the port is situated a mere six hours steaming from the east-west 
shipping lanes), along with natural deep water, gives it a competitive edge to 
reach out to its customer base, including the international container shipping 
community who use the port as their transit hub for cargo flows to the Gulf, 
Indian Subcontinent, Red Sea and African destinations.</font><div align="center">
