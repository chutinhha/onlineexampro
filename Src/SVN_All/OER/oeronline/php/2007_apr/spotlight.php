<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
<br>
<b>Port of Sohar<br>
</b>The modern Port of Sohar, an industrial port, situated 240 km northwest of 
Muscat, has added a new episode in the development of futuristic port facilities 
in Oman. Set up in 2002, the port, along with its container project, is one of 
the largest of its kind in the world. The management of the port is under the 
Sohar Industrial Port Company (SIPC), a 50-50 joint venture between the 
government of Oman and the Port of Rotterdam, the Netherlands. The port is run 
according to highest international standards and it received the prestigious 
DIMA Award for Outstanding Port Management last year. To enhance container 
terminal capacity of the port, the Oman International Container Terminal (OICT), 
a joint venture with Hong Kong-based Hutchison Port Holding, Oman government and 
Netherlands-based Steinweg, has been set up that presently has a container 
handling capacity of 800,000 TEU per annum. Two other terminals handle general 
cargo, dry bulk (Steinweg) and liquid bulk (Oiltanking Odfjell).<br>
<br>
The development of the Sohar port project has been planned in three phases. 
While Phase 1 involved the construction of two main breakwaters, dredging of the 
port basin and the entrance channel, construction of the initial quay wall and 
the liquid bulk jetty happened during Phase 2. According to SIPC’s CEO Jan 
Meijer, the port is currently undergoing its third expansion, which will 
increase the container handling capacity to over 2.1 million TEU per annum. The 
completion of Phase 3 is scheduled until October 2008, Meijer says adding, the 
number of berths in the port will almost be doubled by it, from 12 to 22. The 
work is done by a consortium of the Dutch dredging company Van Oord, a joint 
venture of Six Contruct (Belgian) and Interbeton (Dutch). While the work has 
been commissioned by MOTC, Halcrow is the engineering consultant, and SIPC is 
involved as landlord and advisor to MOTC.<br>
<br>
Sohar’s USP lies in its geographical position — being at the central position in 
the booming triangle of Abu Dhabi, Dubai and Muscat. Due to its strategic 
location just outside the Strait of Hormuz, which is a major traffic hub for oil 
and gas shipments, the Port of Sohar allows ships to avoid increased insurance 
premiums, making it an ideal logistics hub. <br>
<br>
<b>Integrated development model<br>
<img border="0" src="spot2.jpg" width="250" height="188" align="right" hspace="6" vspace="4"></b>One 
of the exclusive features of the Port of Sohar is that its management is based 
on the business proposition of ‘landlord port’ so as to maximise the involvement 
of the private sector. As per the proposition, which has its counterparts in 
Rotterdam, Antwerp, New York, Singapore, Saudi Arabia, Iran and India, land and 
waterfront infrastructure are leased to private companies and industrial 
production, cargo handling and other operations are undertaken by specialised 
companies as they can offer quality and competitive rates. This allows SIPC to 
focus on other important segments like issuing rules and regulations and 
establishing the framework for health, safety, environment, emergency and 
security.<br>
<br>
The Port of Sohar is one of the world’s larger port development projects, 
currently spread over 2000 hectares and involving investments exceeding US$12 
billion (for the period 2002-2008). Investments worth billions of dollars are 
still in the pipeline to develop additional port facilities, heavy industries 
and utilities. The industrial projects in the Sohar port industrial area, a 
green-field industrial complex, are clubbed as one of the largest in the Middle 
East region and would go a long way in achieving the long-term strategy to 
diversify Oman’s economy as per the Oman Vision 2020.<br>
<br>
Within a few years of its establishment, the industrial port of Sohar today is 
flooded with huge international investments in industries and infrastructure 
projects around it to the tune of billions of dollars. The industrial port has 
been feeding the regional economy in particular and the national economy in 
general, by supplying raw materials and commodities and carrying the finished 
goods to different markets of the world, from a number of petrochemical, 
logistic and metal industries that have cropped up around the port in recent 
times.<br>
A number of industrial units coming up in Sohar are gas based. The industries 
(see box) have been established in three clusters — petrochemical, logistics and 
metal industries — to ensure integrated production and marketing. The 
petrochemical cluster of Sohar port project consists of a refinery and 
production plants for aromatics, formaldehyde, methanol, polyethylene, 
polypropylene, urea etc, while the metal cluster includes a steel complex and 
aluminium production. The most important rationale behind this integrated 
development model is to ensure economies of scale by reducing the operating cost 
and optimum utilisation of infrastructure for industrial units, as opposed to 
creating infrastructure and logistics service for different industries 
separately. The industrial zone has also been equipped with local infrastructure 
facilities so that there is no dependence on sourcing them from other areas. <br>
<br>
Similarly, the Salalah Free Zone Company (SFZC), established by the government 
adjacent to Salalah port, is all set to offer a professional environment, access 
to Salalah seaport and international airport facilities, ample labour, banking 
outlets, property management and 24-hour security. The SFZ, currently being 
developed in multiple phases over an area of 2,000 hectares, will offer a mix of 
industrial, manufacturing, warehousing, logistics, distribution, R&amp;D and office 
facilities, retail outlets, resort, and residential space, after it’s fully 
operational. The Phase 1 will be spread over 200 hectares, providing logistics, 
freight forwarding and manufacturing facilities, including infrastructure 
facilities like roads, power, water, etc. <br>
<br>
The management of the SFZ, officially launched in 2005, rests with Salalah Free 
Zone Company SAOC, a government-owned body. The government has extended many 
incentives at the free zone, including 100 per cent foreign ownership of 
investment of projects, import duty exemption and exemption from income tax for 
30 years and from minimum capital limit. Octal Petrochemical has set up the 
first project in the free zone and has already started commercial production. 
Recently, SFZC signed an agreement with India-based company Dunes Oman to set up 
a 12-hectare manufacturing unit at Salalah Free Zone for the production of 
automobile brakes, and in March the foundation stone for the project was laid.<br>
<br>
<b>The way forward<br>
</b>In recent years, visits by tourist vessels/cruise liners at the Port Sultan 
Qaboos have become more frequent, thanks to the expanding tourism sector in the 
Sultanate. In response, construction of a cruise terminal is underway at the 
port and it is expected to be ready by the end of the third quarter of this 
year. There are also plans to set up a duty-free shopping facility in the new 
terminal that would provide value-added services to international cruise 
tourists. Moreover, the Port Services Corporation has also carried out a 
feasibility study for constructing a new deep-draft berth and additional storage 
space to accommodate increased traffic. <br>
<br>
In the Sohar port, where OICT recently opened its Terminal B of Phase 1, the 
next 2-3 years will be marked by building and expansion activities. Large parts 
of the Sohar Industrial Port have been leased, though approximately 20 per cent 
of the land is still available. The ongoing Phase 3 of the port’s expansion 
comprises:</font><ul>
  <li>
  <p align="justify" style="margin-left:-11px; margin-right:5px; margin-top:3px; margin-bottom:3px">
  <font face="Verdana" size="1">The quay wall extension by 1,900 metres (Sohar 
  Aluminium 300 metre, Shadeed Iron &amp; Steel 600 metre and Oman International 
  Container Terminal 970 metre) </font></li>
  <li>
  <p align="justify" style="margin-left:-11px; margin-right:5px; margin-top:3px; margin-bottom:3px">
  <font face="Verdana" size="1">Additional dredging of 20 million m3 to deepen 
  the port to 18 metre, combined with land reclamation, to allow the largest 
  vessels to call at the port of Sohar;</font></li>
  <li>
  <p align="justify" style="margin-left:-11px; margin-right:5px; margin-top:3px; margin-bottom:3px">
  <font face="Verdana" size="1">The reconstruction of Harmool harbour, breaking 
  down a 400-metre breakwater and constructing a new 500-metre breakwater;</font></li>
  <li>
  <p align="justify" style="margin-left:-11px; margin-right:5px; margin-top:3px; margin-bottom:3px">
  <font face="Verdana" size="1">The construction of 300-metre heavy load 
  platform for Larsen &amp; Toubro (L&amp;T). In October 2006, L&amp;T, together with an 
  Omani partner, decided to build a construction site for heavy offshore 
  equipment like drilling platforms, providing jobs for 800-1000 people 
  (possibly more after extension).</font></li>
</ul>
<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1">The 
Salalah port is also undergoing expansion. This includes two berths for 
970-metre continuous linear quay; 18-metre water depth, with 18.5 metres in the 
approach channel, and 10 gantry cranes with a <br>
23-container outreach. In addition to the above three ports, there is also the 
Port of Khasab in Musandam region, which is currently being expanded at the cost 
of RO15.8 million to cater to the economy of Musandam Governorate in particular, 
and the Sultanate’s economy in general. Besides, the government has recently 
signed RO46-million loan agreement with the Arab Fund for Economic and Social 
Development to finance the development of the Al Duqum port complex, located in 
the Al Wusta region. There are also plans to develop a dry dock near the port at 
the cost of RO100 million. Besides these ports, there are also specialised ports 
such as Mina Al Fahal, specialised in the export of oil, Qalhat Port for the 
export of LNG and other small ports in Shinas and Sur.</font></p>
