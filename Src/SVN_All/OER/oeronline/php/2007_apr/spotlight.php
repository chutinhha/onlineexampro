<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
</font><b><font face="Verdana" size="4">The seafaring heritage<br>
</font></b><font face="Verdana" size="1"><b>The ports of Oman have taken giant 
strides in leading the country’s flotilla towards the goal of economic 
diversification, reports Sunil Kumar Singh <br>
&nbsp;</b></font><p align="justify" style="margin: 3px 10px">
<img border="0" src="spot1.jpg" width="350" height="187" align="left" hspace="6" vspace="4"><p align="justify" style="margin: 3px 10px">
<font face="Verdana" size="1"><b><br>
</b>The Sultanate of Oman — flanked by the Straits of Hormuz in the north and 
the Arabian Sea in the east with the coastline stretching for 1,700-odd km — 
prides itself on being a richly bestowed country especially in terms of marine 
resources. Its grand seafaring tradition and trade with the outside world dating 
back thousands of years owe much to the existence of the distinctive port 
infrastructure dotting the coastline of the country. The ports of Salalah, 
Muscat and Sohar have all along been the hub of trading with Africa, Europe, 
Egypt, India, Far East and other parts of the globe and they continue to drive 
Oman’s economy towards greater heights even after hundreds of years of existence 
— an extraordinary success story of an equally extraordinary maritime tradition.
<br>
<br>
Today, the spectacular advance made by the government and the private players in 
putting into place a modern port structure is at the heart of Oman’s transition 
from a primarily oil-based economy to a diversified one. These ports have been 
the carriers of a comprehensive feeder network to Oman’s industries for inbound 
and outbound cargo movement. In essence, the milestones accomplished by the 
ports of Oman through the centuries present a microcosmic depiction of the 
country’s glorious strides towards progress and development. We take a look at 
the success stories of Oman’s ports that have heralded the country towards a new 
course.<br>
<br>
<b>Port Sultan Qaboos<br>
</b>With the dawn of Renaissance in 1970, a new wave of development activities 
patronised by His Majesty Sultan Qaboos bin Said started to blow across 
different sectors, such as economy, social welfare, health, education, 
communication and transportation. Oman’s port sector was one of them and its 
spectacular progress owes much to the continued support extended by His 
Majesty’s government. In fact, the onset of modern chapter in Oman’s maritime 
commerce and development began with HM Sultan Qaboos bin Said inaugurating the 
Mina Qaboos (now Port Sultan Qaboos) port in Muscat in 1974. It’s Oman’s one of 
the premier maritime gateways and also has strategic importance, not only for 
the Arabian Gulf but also the Indian sub-continent and markets in Africa and 
Europe. The port, managed by Port Services Corporation SAOG constituted in 1976, 
is equipped with state-of-the-art infrastructure, skilled personnel and 
efficient container handling operations.<br>
<br>
Port Sultan Qaboos serves as the main commercial port of the Sultanate, whereas 
the two other ports of Sohar and Salalah are mainly transhipment hubs and 
industrial ports. During 1977–2005, the total imports handled by port were about 
39.5 million DWT, accounting for 46.4 per cent of the Sultanate’s total imports. 
On the other hand, the total exports were more than six million DWT, that is 34 
per cent of total exports. <br>
<br>
In the last decade, the port fully capable to accommodate containerised 
shipping, witnessed an overhaul in terms of infrastructure development and 
service enhancement. The slew of modernisation projects has catapulted it into a 
state-of-the-art port bustling with container handling activities ranging from 
containerised goods to cars and cruise ships. The port now acts as the backbone 
of the consumer market in the capital city of Muscat as well as its hinterlands, 
and is capable of handling 300,000 TEU per annum. In the years to come, it 
further plans to give a fillip to its container handling capacity in view of the 
growing volumes in imports and exports.<br>
<br>
<b>Port of Salalah<br>
</b>Created in 1998 out of Mina Raysut, a small fishing port in the southern 
region of Oman, the vision behind creating the port of Salalah was to build a 
world-class container terminal that would give a fillip to the economy, 
employment rate and tourism as well as to facilitate Oman to become a global 
trading hub. <br>
<br>
The Salalah Port project, a 70:30 joint venture between the government of Oman 
and the A.P. Moller-Maersk Group with its headquarters in Copenhagen, is the 
largest privatisation project in Oman. </font>
