<p align="justify" style="margin-left: 10px; margin-right: 10px">
<font face="Verdana" size="1"><b>FDI in 12 industrial clusters</b><br>
Since 2002, the major multinationals investing in the top 90 projects in Oman 
have been from five countries -- India in 17 projects (23 per cent), UAE in 10 
(14 per cent), USA in 9 (12 per cent), and four each (5 per cent) from Qatar and 
Egypt respectively. Investors from various other countries total the remaining 
40 per cent FDI in 29 assorted projects. <br>
<br>
The top dollars have gone to Business and Financial Services -- 19 projects 
attracting 21 per cent of the FDI coming into Oman since 2002. Joint 2nd come 
Heavy Industries, and Property/Tourism/Leisure with 15 projects each, getting 17 
per cent foreign investment share respectively. Also hitting the double digit is 
the Chemicals/Plastics and Rubber industrial cluster, with 10 projects carving 
out 11 per cent of the FDI pie.<br>
<br>
The remaining eight of the dozen industrial clusters are Food/Beverages/Tobacco 
and ICT (7 projects each, with 8 per cent share respectively), Electronics and 
Light Industry (5 projects each, with 6 per cent share respectively), 
Logistics/Distribution and Consumer Products (2 projects each, with 2 per cent 
share respectively). Life sciences (2 projects) and Transport equipment (1 
project) make the rest 3 per cent of the FDI inflow over the past five years.<br>
<br>
Moreover, the key 13 business sections in the 90 monitored projects drawing FDI 
allocations since 2002 are:<br>
<br>
The volume of Omani non-oil &amp; gas exports rose form R.O.173.29 million in 1996 
to R.O.555.3 million in 2005, a 14 per cent average growth rate undoubtedly 
facilitated by the successful inflow of FDI in the key industrial clusters and 
business sections. As the official figures show, the FDI inflow were definitely 
a factor in helping to build 29 Omani products -- and export figures touched 
R.O.349 million in 2005.<br>
<br>
<b>Single Window Processing</b><br>
The impact of FDI depends on a multitude of factors, such as the level of 
technology used in domestic production in the host country, the level of 
education of the host country workforce, the level of financial sector and 
institutional development, etc. All these factors and more contribute to whether 
the host country in question can “absorb” and hence benefit from FDI.<br>
<br>
A transparent and well-regulated economic system makes the Sultanate one of the 
most liberal and attractive markets in the Arab world. To attract FDI, the 
Sultanate has created a single window for processing all transactions and 
implemented a system to link all governmental units. “This ensures smooth 
running for over 96 per cent of the transactions,” says H.E. Maqbool Sultan. <br>
<br>
Another key factor for foreign investors is the setting up of six industrial 
estates in different parts of the country. Sohar industrial area now has an area 
of 7 million square metres. Basic infrastructure projects at Buraimi industrial 
area, with an area of 4.5 million square metre, is almost complete. Expansion of 
Nizwa, Rusail industrial areas and the Information Technology Park, termed 
Knowledge Oasis Muscat (KOM), is also underway.<br>
<br>
FDI-linked development of the country’s infrastructure is on a fast-track – the 
5th largest container port in the world at Salalah, an industrial port at Sohar, 
excellent road and air links, and world-class telecommunication facilities. Mina 
Sultan Qaboos, Muscat, is a well-established port that is equipped to handle any 
kind of vessel efficiently. A passenger terminal and a duty free zone will 
extend the range of services provided to users of this port.<br>
<br>
Together with the new developments, the Government has also charted a plan to 
upgrade existing hubs, and identify and develop other locations along the long 
coastline of Oman. <br>
<br>
Key incentives consist of;<br>
• Upto 100 per cent ownership,<br>
• Soft loans with low interest rates and easy payback option,<br>
• Exemption from customs duty on import of plant and equipment,<br>
• Relief from customs duty on raw materials,<br>
• No personal income tax,<br>
• Corporate tax holiday up to 10 years,<br>
• Full repatriation of capital, net profit and royalties, and<br>
• Export credit insurance through the Export Guarantee and Financing Agency 
(EGCA)<br>
<br>
Increased financial sector liberalisation and
private participation are the most likely policies that can facilitate the 
development of financial sector. The theoretical underpinnings of the link 
between 
financial development and economic growth can include mobilising resources, 
diversifying risk,
improving corporate governance, reducing 
transaction and information costs, and promoting specialisation.</font></p>
