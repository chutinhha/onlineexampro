<p align="justify" style="margin-left: 10px; margin-right: 10px">
<font face="Verdana" size="1"><b>Till the year 2002, the common complaint of market analysts was that 
Foreign Direct Investment in Oman will only grow if it’s allowed to happen. The 
corollary was that things could be a lot better if only so many sectors were not 
‘off limits’ to FDI. Over the last five years, there has been a quantum change 
in this scenario. What has brought about this change – and what more needs to be 
done? Abhijit Sinha reports… </b></font></p>
<p align="justify" style="margin-left: 10px; margin-right: 10px">
<font face="Verdana" size="1">The World Bank ‘Doing Business’ report puts Oman 55th among 175 countries in 
regard to flexibility and ease in doing business, and the 2nd Arab Business 
Intelligence report ranks Oman 3rd amongst 22 Arab nations that inspire most 
economic confidence. Foreign Direct Investment (FDI) in a country is a 
reflection of that confidence – that is, building a long-term relationship 
between two commercial enterprises of two different nations. <br>
<br>
Now, with the Oman-US Free Trade Agreement (FTA) scheduled to come into force by 
July, the confidence level of foreign investors is bound to go up. The long 
drawn out process of the Gulf Cooperation Council (GCC) FTA with the European 
Union (EU) is now entering its final phase, and is likely to be in place by the 
end of 2007. With GCC FTAs with India, Pakistan, Japan and Singapore on the 
anvil, the region – and especially Oman -- will definitely see more FDI coming 
in.<br>
<br>
FDI has come to be regarded as a means to achieve economic development in its 
own right, with expected positive spillovers over and above those associated 
with domestically financed investments. Industrial monitors say that Oman has 
had the munificence of attracting 37 FDI projects worth US$ 837.70 million in 
2006, and four projects worth US$ 700 million have been logged in 
January-February 2007.<br>
<br>
H.E. Maqbool Sultan, minister for commerce and industry, told OER: “In 2006, the 
foreign investment Oman totaled US$ 2.7 billion (approx R.O. 0.9 billion), while 
FDI was 41.8 per cent of the total investment – and this included 21 per cent 
for the commerce and industry sector.” This inflow of foreign capital has 
benefited the private sector, whose contribution during the 6th Five-Year plan 
grew by 8.5 per cent, more than the average annual rate of the added value.<br>
<br>
In keeping with His Majesty Sultan Qaboos’ Vision 2020, the emphasis is on 
privatisation programme and infrastructure -- and on strategic industries like 
gas, tourism and IT. A number of projects worth US$12 billion are underway in 
Sur and Sohar that include Oman Polypropylene, Polyethylene, an oil refinery, 
aluminium smelter, iron project and a methanol project. “Initial estimates of 
potential exports are around R.O. 970 million per annum,” said the minister of 
commerce and industry.<br>
<br>
On the volume of investment in infrastructure projects, H.E. Maqbool Sultan 
said, “As per the 7th Five-Year plan, this volume is expected to be more than 
R.O. 13 billion – and investment in major tourism projects like the Blue City 
and Ras Al Had is conservatively estimated to be more than R.O. 15 billion in 
the next 10 years.”</font></p>
