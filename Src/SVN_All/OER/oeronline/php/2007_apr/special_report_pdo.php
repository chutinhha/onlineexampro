<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
</font><b><font face="Verdana" size="4">Partners in Progress</font></b><font size="1" face="Verdana"><br>
<b>Petroleum Development Oman (PDO), with its extensive social investment programme, 
has made a key contribution to the sustainable development of the Omani society. 
OER reports</b><br>
<br>
<img border="0" src="special7.jpg" width="283" height="193" align="left" hspace="6" vspace="4">The companies’ involvement in promoting corporate responsibility and providing 
economic opportunities has clearly become an emerging trend across the globe. 
Integrated Impact Assessment (11A) reflects an investing approach that 
integrates social and environmental concerns into investment decisions. In the 
Sultanate of Oman, Petroleum Development Oman (PDO), the leader in oil &amp; gas 
industry, is a prime example of how a company contributes to the sustainable 
development of the society by integrating Social Investment in its overall 
business objectives.<br>
<br>
“PDO’s size and its role in the economy mean that we are inextricably linked to 
Omani society as a whole. Social partnership is a key element of our company 
ethos,” said John Malcolm, Managing Director, PDO. <br>
<br>
PDO’s social investment programme is based on the premise of partnership with 
the Government and the local authorities for the benefit of the Omani people. In 
2003, the Government of Oman established a Social Investment Committee 
representing the Omani Government and PDO. The committee assesses and evaluates 
proposed social performance projects and monitors progress of the approved ones.
<br>
<br>
<b>Strategic Social Investment (SSI)</b><br>
<img border="0" src="special8.jpg" width="225" height="151" align="right" hspace="6" vspace="4">The strategic social investments of PDO are focused on activities which are 
linked to the impact of its operations and enhancement of related opportunities 
to the local communities. A shining example of PDO’s SSI is Local Community 
Contractors (LCC) initiative. It gives people living in the concession area 
opportunities to establish their own companies that could provide services to 
PDO thus increase the in-country value and maximize the benefits to the local 
communities.<br>
<br>
Under this programme, around 60 companies have been established providing 
employment to more than 1,000 Omanis and generated business worth US$200 million 
so far. Many of these local enterprises have gained ISO certification. In 
December 2006, PDO’s LLC programme won the coveted Sheikh Mohammad bin Rashid al 
Maktoum Award for Arab Management. <br>
<br>
<b>Social Investment within Projects</b><br>
In order to integrate social programmes in all business aspects, each major 
project that PDO carries out has a social performance plan. Every major project 
has a full Sustainable Development Focal Point and Community Relations Officer 
to manage impacts and realize social investment benefits. <br>
<br>
Several initiatives stand out as examples of this approach. In the 48” Pipeline 
Increase Initiative, PDO employed local women to act as patrol officers along 
the length of the pipeline’s construction. A maternity room was created at Al 
Kamel Health Centre and maintenance of Aflajs was provided in Bidiyah. <br>
<br>
In the Harweel Development project, PDO helped in reviving an ancient skill by 
providing training for women in the art of camel bone carving. “The project not 
only confines its benefits to the financial returns it generates but also 
revives a traditional craft that was about to become extinct. This in itself is 
a valuable service which the company is offering to the community <br>
<br>
PDO also built a new veterinary clinic in Thumrait besides building a laboratory 
extension of the Shelim Veterinary Clinic providing employment to the locals.<br>
<br>
<b>Corporate Social Investment Projects <br>
</b>
<img border="0" src="special9.jpg" width="300" height="188" align="left" hspace="6" vspace="4">PDO contributes to the society, not only through the production of oil and gas, 
but also by enhancing social development and implementation of projects of 
benefit to society as a whole. One of PDO’s small business establishment 
initiative near Salalah involved training of 20 women from remote villages in 
the Dhofar Governorate in the arts of tailoring, cosmetics and embroidery. <br>
<br>
For the people with special needs, PDO financed the installation of an 
information and communication technology centre at Omar bin Al Khatab Institute 
for the Blind. The company also provided a multimedia unit to the Ministry of 
Education for producing high quality multimedia education materials for use by 
students across Oman. <br>
<br>
<b>Grants &amp; Donations<br>
</b>PDO has an active Grants and Donations Committee that provides financial support 
to different initiatives for the benefit of the society. The company donates 
money every year in response to around 500 requests that it receives for support 
for various educational, sporting and health-related events as well as for 
numerous conferences and exhibitions. <br>
<br>
PDO has donated personal computers to schools, built sunshades for school play 
areas and donated photocopy machines and water coolers to educational 
establishments across the Sultanate. It also sponsors health education campaigns 
such as Muscat Health Festival and Diabetic Awareness campaign in the interior.
<br>
<br>
<b>Gift to the Nation <br>
</b>PDO has a long tradition of marking the milestone by giving a gift to the nation 
every five years. So far, the company has established the Oil &amp; Gas Exhibition 
Centre, Planetarium and Public Knowledge Library that have added immeasurably to 
the education of Omanis young and old, and Traffic Safety Institute that helps 
in reducing road traffic accidents to save lives. <br>
<br>
<b>Recent Initiatives<br>
</b>
<img border="0" src="special10.jpg" width="250" height="168" align="right" hspace="6" vspace="4">Talking about PDO’s recent social investments, Malcolm informed, “In 2006, PDO 
signed 11 memoranda of understanding (MoU) with official organizations, 
committing RO1.5 million for a range of initiatives in education, healthcare, 
fisheries, traditional crafts and community development. Collectively, the MoUs 
represent the largest package of social development initiatives financed in one 
go by PDO.” <br>
<br>
PDO has started the year 2007 by signing MoUs for providing financial support 
for four social investment projects. The projects include the development of the 
Al-Hujira Craft centre in Al Suwaiq, the provision of entrepreneur training in 
Dhofar, funding of an awareness campaign for the Environment Society of Oman 
(ESO) and the carrying out of a Palaeolithic survey in Central Oman.<br>
<br>
The Al-Hujira Craft Centre will create jobs for women and help to create new 
markets for regional Omani handicrafts. The small-business training PDO is 
supporting is being organised by the Dhofar Governorate. A total of 50 people 
will be trained on how to set-up and run small businesses. <br>
<br>
PDO’s support of the ESO will allow the society to hire a “champion” to 
undertake environmental awareness work in Muscat. The support for the 
Palaeolithic survey of central Oman will help deepen the understanding of Oman’s 
early history. The project will involve a systematic survey of the Huqf area 
with the aim of producing an archeological map of this region.</font><p align="right" style="margin: 3px 10px"><font size="1" face="Verdana"><br>
