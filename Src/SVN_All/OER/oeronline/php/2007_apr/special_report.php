</font><b>
<font size="4" face="Verdana">Setting The Trend </font></b>
<font size="1" face="Verdana"> <br>
<b>In five years, Oman Society for Petroleum Services (OPAL) has become a major 
force in promoting professionalism, competition, human resource development, and 
lots more in the oil and gas sector. Soud Al-Marhuby, Executive Manager, HRD, 
OPAL, analyses the organisation’s role</b><br>
<br>
Through its formation phases, a small but determined group of companies met 
regularly as a forum to exchange ideas and best practices in the petroleum 
industry. The focus then was, and still is, on four critical business areas, 
namely human resources management, best business practices, communication, and 
health, safety &amp; environment. Participating companies had a very strong belief 
that standards in all these four areas could be improved if they worked together 
and adopted a Code of Practice they would all adhere to. It was then decided 
that such ambitions could not be realised by an unofficial body and a decision 
was made to seek formal registration as a non-governmental organisation (NGO) 
that would represent the petroleum industry in the Sultanate of Oman.<br>
<br>
<b>Holistic approach</b><br>
On October 27, 2001, the organisation was registered by the Ministry of Social 
Development under the regulations for forming societies. OPAL was then given its 
official name of Oman Society for Petroleum Services. OPAL has since then 
developed rapidly and continuously, expanding its membership base to over 200 
companies and creating true partnerships with all its stakeholders in order to 
accomplish its mission. Most of the achievements have been through the policy of 
working together and getting a consensus on projects being undertaken, and then 
accepting the resulting decisions. <br>
<br>
Alignment of the vision and objectives of OPAL membership with the national 
aspirations is an excellent principle to have, and it symbolises the governance 
of OPAL. OPAL has a Board, a Steering Committee and a Secretariat with CEO and 
Executive Managers, who execute the Board’s strategic decisions. The Steering 
Committee gives guidance and regularly reviews progress. <br>
<br>
Thoroughly dedicated feasibility studies plus a well-prepared model are keys to 
the principles with which OPAL proceeds with projects that have achieved so much 
in the last five years. A fit-for-purpose training model was constructed to 
supply continuous flow of skills that were specifically needed in the oil and 
gas sector. The model was presented to the Ministry of Manpower for approval and 
for budgetary assistance. That single project led to training, and guaranteed 
employment of over 5,000 Omanis in the petroleum sector.<br>
<br>
<b>A body of standards</b><br>
In its infancy, OPAL concentrated on training and development of human 
resources. This effort coincided with the development of HSE (Health, Safety, 
and Environment) Management System (MS) for member companies. <br>
<br>
The HSE-MS became the first OPAL Standard that was used to comply with the OPAL 
Code of Practice. This was quickly followed by the OPAL Compliance Verification 
Certificate (CVC) which is awarded to member companies who have demonstrated 
achievement of best employment practices to a given OPAL Standard in five areas–Omanisation; 
Manpower Development; Legal Requirement; Remuneration; and, HSE. The vision here 
is that member companies will operate at world-class standards. The CVC Standard 
therefore has a dynamic nature in that levels of compliance are raised annually. 
OPAL, thus, within a very limited period of time, has established itself as a 
body of standards for member companies. The objective was to create a 
level-playing field that was fair and transparent in a highly competitive 
business. <br>
