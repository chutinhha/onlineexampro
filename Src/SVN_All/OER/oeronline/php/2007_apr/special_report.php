<br>
OPAL then created a portal to enable easier and effective communication between 
members, and also to promote corporate OPAL, its vision, strategies and 
standards. OPAL as an NGO needs to continually enhance its credibility not only 
amongst members but also with the government and the Omani people. This is one 
organisation that covers the whole petroleum sector and is unique in the 
Sultanate in terms of membership, transparency in governance, non profit-making, 
and a force for good. In order to align training and development efforts, OPAL 
has established a competence framework, OPAL Competency Card (OCC), principally 
for technical disciplines with a view to maintaining and upgrading skill sets in 
the industry. Ultimately, this will enable member companies to map competency 
levels in their business units and bridge any competence gaps in order to 
improve quality and performance and, hence, company productivity.<br>
<br>
In the course of the five-year period, OPAL also addressed several challenges. 
These include:<br>
• Omanisation Plan for the oil and gas sector<br>
• Transferability concerns of employees from one contractor to another<br>
• Redundancy issues<br>
• Occupational health and safety regulations<br>
• Nawras-OPAL special relationship based on purchasing power<br>
• Overtime issues<br>
• Promoted HSE<br>
<br>
<img border="0" src="special4.jpg" width="299" height="211" align="right" hspace="6" vspace="4"><b>Future challenges</b><br>
Challenges that face OPAL in the immediate future include the forthcoming 
establishment of labour unions in the Sultanate of Oman. New methods, coupled 
with a mindset change, will be required to deal with employee and industrial 
relations. To this end, OPAL is engaged in mechanics that are being developed 
for establishing labour unions in the Sultanate and is working on a principle 
which will deliver a win-win situation in contrast to a confrontational one; in 
other words, a uniquely Omani Labour Unionism. The industry also needs a new 
breed of Human Resources Practitioners who are fully competent and capable to 
raise HR function into a strategic position in the companies and to integrate 
its activities with company business. Here, OPAL will provide tutorials at 
different competence levels and certify HR practitioners who can demonstrate 
those competences. </font><hr width="90%" size="1">
<p align="justify" style="margin: 3px 10px"><font size="1" face="Verdana"> <br>
</font><b><font face="Verdana" size="4">OPAL has benefited its members </font>
</b><font size="1" face="Verdana"> <br>
<b>Adil Abdulaziz Al-Kindy, CEO, Oman Refinery, talks about OPAL’s contribution, 
the proposed merger with Sohar Refinery and future plans</b><br>
<br>
<b>
<img border="0" src="special5.jpg" width="147" height="191" align="right" hspace="6" vspace="4"></b>OPAL’s constant endeavours towards providing training to Omani people, building 
an industry wide consensus, implementing common code of practice, maintaining 
high QHSE standards, and above all, promoting best business practices have taken 
Oman’s oil and gas industry to a new high. Besides, they have also benefited its 
members. As regards Oman Refinery, the company has established itself as a 
leader and model for industrial excellence in the oil and gas sector. The 
company has achieved this strategic aim by adopting best practices and 
conforming to international standards in various aspects of its business. In a 
move that would further cement the company’s leadership position in the oil 
industry, Oman Refinery Company will merge Sohar Refinery Company with itself. 
The merger will maximise the value of the two refineries for the country and 
stakeholders. Specifically, the merger will provide an effective platform for 
growth and unity of direction, and focus on optimisation of operations, HSE 
management, effective production planning, and realisation of synergies and 
common business objectives.<br>
<br>
Besides this, the main focus of the company is now on value-added petrochemical 
products. Studies are in progress for an ethyl-benzene plant as well as an 
N-Paraffin plant in Sohar. A bitumen plant is also being considered. The company 
is seeking to expand the Sohar Refinery to supply the required feedstock for new 
petrochemical industries. Additionally, it is studying the feasibility of a mega 
refining (up to 300,000 barrels per day) and petrochemical industrial complex in 
Duqum. The company has already made its mark in a big way by achieving certified 
conformances to ISO 9001 for Quality Management, ISO 14001 for Environmental 
Management, OHSAS 18001 for Occupational Health and Safety Management, and ISO 
27001 for Information Security Management.</font><hr width="90%" size="1">
<p align="justify" style="margin: 3px 10px"><font size="1" face="Verdana"><br>
</font><font size="4" face="Verdana"><b>‘We need to do more to simplify common standards’</b></font><font size="1" face="Verdana"><br>
<b>
<img border="0" src="special3.jpg" width="147" height="210" align="left" hspace="6" vspace="4">Shahswar G. Al Balushi, CEO, OPAL, shares his vision on human resource 
development and the challenges before Oman’s oil and gas sector in a 
conversation with Sunil Kumar Singh<br>
</b>
<br>
<b>Is the diminishing supply of skilled personnel in the oil and gas sector in Oman 
coming in the way of Omanisation?<br>
</b>Diminishing supply of skilled personnel is an international trend that has 
affected all producing countries. Finding skills from the international market 
has become as challenging as Omanisation and, in fact, more expensive. This 
trend has made it clearer that it is imperative that we should grow our own 
timber and think aggressively towards up-skilling the local workforce in order 
to become less dependent on international workforce. With all the efforts that 
are being made on training and development by everyone, we will see a dip for a 
short period and the market will adjust with growth resuming at a healthy pace.
<br>
<br>
<b>How are you coping with the issue of skill shortage?<br>
</b>The focus is on training, internal development, up-skilling and re-skilling. The 
quality of outcomes from different training institutions has improved; this will 
allow new employees to develop faster and adapt better to new technologies. We 
are also discussing with the government to be flexible in the coming period on 
Omanisation targets. We have launched the OPAL Competency Card, a system that 
will help map the competency of the sector workforce. This will allow us to 
better determine our skill needs and plan for future development and demands.
<br>
<br>
<b>What are the challenges and areas of improvement in Oman’s oil and gas industry?<br>
</b>Going by the nature of international as well as natural resource trends, Oman’s 
oil and gas sector is facing a few challenges. <br>
<br>
(1) Production challenges related to heavy oil and diminishing resources can be 
improved and is being improved through increased exploration activities and 
introduction of new technologies to enhance oil recovery. This challenge is 
opening opportunities for Omani engineers to gain exposure to these new 
technologies allowing them to enhance their skill base. I would advise that 
effort should be exercised to allow this to happen more effectively.<br>
<br>
(2) In the near future, more and more new recruits will be employed in the 
sector; this will affect safety due to drop in experience and competency 
profiles. For the oil and gas sector this is a serious matter. I feel it is 
important that all production and exploration companies should agree to a common 
training programme that will enhance safety and a refresher programme that will 
help improve safety in the workplace. Also OPAL is planning to drive a safety 
culture programme and I would like to see the participation of all the companies 
in this programme as it will drive safety culture from the grassroots.</font><p align="justify" style="margin: 3px 10px">
<font size="1" face="Verdana"><br>
(3) Skilled human resource is another challenge, which we have discussed 
earlier. I do not see a halt on the drive on Omanisation as a possible topic for 
discussion; hence we have to come to terms with this fact. Concerted efforts 
should be made to train, up-skill, and re-skill. OPAL is working with the 
government colleges to develop simulation training programmes linked with 
employment. This will help beef up the practical aspects of the training making 
college graduates readier for employment. <br>
<br>
<b>What are the regulatory and industry standards of the oil and gas industry?</b><br>
This is an interesting question particularly now that we have more than one 
producing company. OPAL, through the philosophy of self-regulating, has worked 
on common standards in Health, Safety and Environment Management System through
OPAL-HSE-MS, in Employment Practices through OPAL-CVC, and in competency 
assurance through OPAL-OCC. I believe we need to do more in order to have 
simplified common standards across the board. This will help refine cost of 
production, enhance efficiency and reduce incidents in the industry.<br>
<br>
<b>What promises does offshore exploration hold for Oman’s oil and gas industry?</b><br>
It is only through aggressive exploration activities, Oman will be able to 
sustain its oil and gas production for longer periods. With present oil prices, 
it is feasible to undertake offshore exploration. And what potential that it 
holds, one has to wait and see.</font><hr width="90%" size="1">
<p align="justify" style="margin: 3px 10px"><font size="1" face="Verdana"><br>
</font><b><font size="4" face="Verdana">Firms acquainted with local mart<br>
</font></b><font size="1" face="Verdana"><b>Simon Karam, Chairman, OPAL, speaks on the organisation’s role, enhanced oil 
recovery technology, alternative energy sources and much more, with Sunil Kumar 
Singh<br>
</b>
<br>
<b>
<img border="0" src="special6.jpg" width="147" height="198" align="right" hspace="6" vspace="4">What roles does OPAL play in attracting new players to explore oil and gas 
resources?<br>
</b>OPAL is a forum, a body of standards, a facilitator and a force for the good. 
OPAL assists new companies in acquainting themselves with the local market, in 
the preparation of their Omanisation programme, in the spreading and 
implementation of best practices, and in making their voice heard within the 
industry as well as with the major stakeholders, such as ministries and oil 
producers. This basket of benefits, which are readily available to new players, 
encourages them to extend their activities to Oman.<br>
<br>
<b>How is OPAL gearing up to achieve the oil production target of one million 
barrels per day by 2012? </b> <br>
• To increase oil production, it is imperative to increase our skilled 
resources. OPAL is doing this mainly in three areas: Raising the standards of 
the oil and gas industry to become world-class, and thus attracting more skills 
to the Sultanate.<br>
• Assisting the industry in the development of human resources, up-skilling the 
local workforce and ring-fencing the technicians, verifying their competencies 
and training needs. <br>
• Providing specific training for the industry, using government funds through a 
Memorandum of Understanding between OPAL and the Ministry of Manpower.<br>
<br>
<b>How far can enhanced oil recovery (EOR) technologies ensure sustained oil supply 
in Oman? </b> <br>
It is a known fact that the extraction of oil in Oman is difficult and costly. 
We have not discovered very large oilfields, as is the case in many countries of 
the Middle East. Oil recovery technologies in Oman are putting hope on heating 
the reservoirs of heavy oils with steam in order to reduce the viscosity of 
crude and, therefore, facilitate pumping. This, however, increases costs. 
Subject to oil prices remaining at a certain level, EOR technologies will help a 
lot in sustaining production.</font><p align="justify" style="margin: 3px 10px">
<font size="1" face="Verdana"><br>
<b>What is the potential for developing alternative sources of energy in Oman? <br>
</b>From what is known to-date in terms of geological exploration, there are no 
serious competitors to fossils fuels at the moment in the Sultanate. 
Hydro-electric projects are non-existent and nuclear energy is not considered to 
be a viable option.</font><p align="justify" style="margin: 3px 10px">
<font size="1" face="Verdana"><br>
This really leaves researchers with the possibilities of further exploring solar 
energy, tidal energy, and wind energy. These are long-term prospects. OPAL, at 
present, does not have any technical R&amp;D activities.</font><p align="justify" style="margin: 3px 10px">
<font size="1" face="Verdana"><br>
<b>What are OPAL’s future plans? <br>
</b>In its first five-year business plan, OPAL developed a series of projects aimed 
at achieving the objectives under which it had been set up. In the present 
five-year plan, the Board members have decided to emphasise implementation and 
consolidation to ensure that all members benefit. <br>
<br>
It is also our plan to assist societies similar to OPAL, by sharing our 
experiences with them. We have already done this in the case of the newly 
registered Omani Society for Contractors (OSC).<br>
<br>
<b>In what ways will the Dolphin Project help Oman’s gas industry in leveraging its 
position?<br>
</b>The Gulf Cooperation Council was formed precisely in view of bringing the six 
countries together in a synergy that will benefit every member more. The Dolphin 
Project will bring in, when needed, a fuel that may have different chemical 
properties than the natural gas produced in Oman. Therefore, more options, more 
stability and more optimism. The GCC spirit will always moderate excesses.</font><p align="right" style="margin: 3px 10px"><font size="1" face="Verdana"><br>
