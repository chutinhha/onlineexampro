<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
</font><b>
<font face="Verdana" size="4">Inspiring Youth to Reach 
GREAT Heights<br>
</font></b>
<font face="Verdana" size="1"><b>New standards for learning and life skills are required to fill the social and 
leadership gaps of our institutions<br>
</b>
<br>
<img border="0" src="mana2.jpg" width="147" height="301" align="left" hspace="6" vspace="4">Pedagogy a word rarely used. And in our age of achievement testing, a bit of a 
lost art. For nearly 15 years I have been a professor in the university system. 
I guess by now I cease to be annoyed by the students who approach school like an 
obstacle course: hurdles they need to crawl under or jump over in order to 
achieve the grade they want.<br>
<br>
I make it my personal challenge each semester to inspire these students to love 
the process of learning. I tell them that they soon will enter a world of work 
that will value individuals who can apply intelligence and emotional tools to 
evolving environments. Memorising a set of data or mastering a skill has a 
value, to be sure. In the 21st century, however, a law of diminishing returns 
immediately kicks in to depreciate the value of those personal assets.<br>
<br>
It jars my students who obsess over the major of study they should choose when I 
proclaim, “It really doesn’t matter what you study, but it does matter immensely 
that you study with curiosity and passion.” At this point, I imagine more than a 
few parents with teenagers roll their eyes and mutter, “If only.” They would be 
content to get their kids to complete their homework, and do so in a way that 
does not feel like pulling out their teeth. If this battleground scene sounds 
all too familiar, your teen sounds like a good candidate this summer for a 
SuperCamp.<br>
<br>
<b>Learning is Fun<br>
</b>Meet Bobbi DePorter, who started the SuperCamps over 25 years ago and continues 
to run them as part of her Quantum Learning Network. More than 43,000 youth are 
now graduates of her camps that focus on learning and life skills. The camps 
operate in 10 countries and take place on top-grade university campuses Stanford 
University and the Claremont Colleges host the camps in California across the 
United States.<br>
<br>
“Most young people are not taught to learn effectively,” Ms. DePorter told me. 
“They do not understand how to discover their strengths, pursue goals, make 
decisions, solve problems and resolve conflicts.” Ms. DePorter launched the 
SuperCamps to give kids the confidence to develop and follow their own interests 
and curiosities. <br>
<br>
Over a 10-day SuperCamp experience, youth learn better strategies for reading, 
writing, public speaking, memorising, note-taking and test-taking.<br>
<br>
Sounds like an intensive summer school, doesn’t it? Yet kids want to keep coming 
back. The SuperCamp experience shows that to pique a kid’s curiosity, it must 
translate into a fun experience. I imagine they are inspired once they look 
inside themselves and find new ways to reach out to everything around them.<br>
<br>
<img border="0" src="mana3.jpg" width="200" height="248" align="right" hspace="6" vspace="4">The SuperCamps engage in some bold myth-busting:<br>
<br>
<b>Myth</b>: Teens waste their minds on video games.<br>
<br>
<b>Reality</b>: Teens decode and create 
games as well as play them.<br>
<br>
<b>SuperCamp Insight</b>: Getting inside video games can help teens reconsider their 
cognitive boundaries.<br>
<br>
<b>Myth</b>: Teens believe they are entitled to whatever they want.<br>
<br>
<b>Reality</b>: Teens yearn to know how to take responsibility to earn their success.
<br>
<br>
<b>SuperCamp Insight</b>: Teen’s struggle for independence is also a search for 
ownership of their personal achievements.<br>
<br>
<b>Expanding the Mind</b><br>
I latched onto Quantum’s programme because it became obvious to me that, with 
notable exceptions, our school system is not preparing young people for a 
society of hyper-connected networks. Many parents and teachers wish that young 
people would not be so mega-sensory oriented and drawn to producing their own 
knowledge more than they are absorbing the classics. Instead of berating youth 
for being wired differently, wouldn’t it be better to help them feel confident 
about themselves and help them to build bridges from classical knowledge to 
emerging ideas?<br>
<br>
Ms. DePorter has a big vision for her SuperCamp. Yes, she is boosting student 
performance. The Wall Street Journal reports that SuperCamp “turns so-so 
students into academic achievers.” Yet her more expansive dream is to set a new 
standard for learning and life skills that fills the social and leadership gaps 
of our institutions. Helping kids gain tools for the little things leads them to 
big ideas that can change the world.</font><p align="right" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
