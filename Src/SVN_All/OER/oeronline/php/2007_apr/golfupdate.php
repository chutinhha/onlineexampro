<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
</font><b><font face="Verdana" size="4">Golf Rush in the Gulf<br>
</font></b><font face="Verdana" size="1"><b>The Middle East, in particular the UAE, tops the list of the best golfing 
destinations in the world. As more and more golfers head to the region, OER 
updates the reader on where to plan the next golf holiday <br>
<br>
<img border="0" src="golf.jpg" align="left" hspace="6" vspace="4"></b>Golf courses in the United Arab Emirates are becoming busier and profitable. 
Even more so than those in traditional European golfing nations such as the UK, 
Ireland, Spain and Portugal. <br>
<br>
According to the inaugural KPMG Golf Benchmark Survey Summary Report, average 
revenues at golf courses in Dubai and the UAE are four times greater than those 
in Portugal, the highest performing country in Europe in this regard. <br>
<br>
While the UAE has 13 golf courses, growing demand from increasing numbers of 
expatriates and tourists, particularly in Dubai, is fuelling rapid growth with 
at least 10 new courses planned or under construction.<br>
<br>
Sultanate of Oman too is bidding to cash in on this ‘golf rush’. The Wave and 
the other such towns being developed in the country boast of state-of-the art 
marina resorts and golf courses, bearing designer signatures. <br>
<br>
The Middle East is truly proving to be golf’s international hot spot, with 
courses in the region averaging 45,000 rounds per year, compared to the 26,000 
rounds played on courses in Great Britain and Ireland, and profitability levels 
(average: 45 per cent gross operating profit) far beyond other courses in the 
seven regions surveyed.<br>
<br>
Case in point is the GolfEx Dubai 2007 where representatives of global golf’s 
major tours as well as commercial industry heavyweights converged late in 
January. In the United Arab Emirates alone, over US$550,000 has been invested in 
golf projects that are currently under planning or construction. The projects 
include the courses in Dubai, Abu Dhabi and RAK. Point to be noted is that these 
neither include the recent re-developments of the Wadi course at Emirates Golf 
Club, The Dubai Creek Golf and Yacht Club and nor does it include the recently 
completed Al Badia Golf Resort and spa and Arabian Ranches. An estimate of all 
the money invested in golf in the UAE since 2000 would produce a figure of over 
US$ 1,000,000,000.<br>
<br>
Add to this US$450,000,000 currently being invested in countries such as Saudi 
Arabia, Oman, Qatar, Bahrain, Kuwait, Egypt, India and Pakistan and it becomes 
clear that business is booming, which in turn has a tremendous positive 
side-effect on golf tourism.</font><p align="right" dir="ltr" style="margin-left: 10px; margin-right: 10px">
