<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
</font><b><font face="Verdana" size="4">Innovation For Growth </font></b><font face="Verdana" size="1"> <br>
</font><b><font face="Verdana" size="4">The Whirlpool strategy</font></b><font face="Verdana" size="1"><br>
<b>For a company constantly under the scrutiny of the financial market, innovation 
in Whirlpool has been a success story, tailored to scale up the mechanisms of 
growth. An OER exclusive<br>
</b>
<br>
<img border="0" src="guest.jpg" width="141" height="337" align="right" hspace="6" vspace="4">Today, for innovation to continually progress, it is necessary to create a 
sustaining culture, standardise tools and processes, align innovation with the 
company’s legacy processes and create scaling mechanisms for growth. For 
ensuring innovation at the lower level and keeping it tuned to innovation, I can 
think of two ways. Firstly, we have found that specifically at the lower levels 
of an organisation, comes the greatest cry and biggest pull for innovation. 
Employees at this level must be given the opportunity to make a significant 
impact on the company in ways they were never able to before. Secondly, 
innovation is not just an outcome but it is a process, and this process can aid 
every employee in bettering performance. Similar to Six Sigma, the tools and 
processes of innovation can be applied to all facets of a business, not just 
products and services. When the finance section is using I-tools to create the 
most effective portfolio of reporting and HR is ideating on the best ways to 
attract and retain talent, you know embedment is occurring. <br>
<br>
While there are many barriers to innovation, the most critical one is top 
leadership support. Innovation is a long-term investment and consistent 
messaging and prioritisation from the top leadership is required. If an 
innovation embedment is purposely halted due to short-term challenges, both real 
results and employee perception will falter and have a ripple effect that will 
long outlast the short-term challenge.<br>
<br>
Senior leaders require a systems approach of surrounding themselves with the 
mechanisms and attaching them to the right path. Not totally different from 
employees at every level, but it requires special care to help them understand 
their leadership role and the new behaviours required.<br>
<br>
<b>Organic growth</b><br>
There certainly has to be some legend, lore, and heroes to have a successful 
culture of innovation, but an intelligent design is critical. Scaling mechanisms 
and consistent communications are critical factors in achieving a company-wide 
culture transformation. With a consistent cadence of communication employees can 
believe this is not a “flavour of the month”. As for scaling mechanisms, it is 
absolutely essential that organic growth begets organic growth. For example, 
training at Whirlpool focuses not only on specific projects but on tool 
facilitation. Therefore each trained person has the chance to create a 10x 
effect on the organisation.<br>
<br>
In Whirlpool, the obvious changes are in the physical look and design of the 
projects and the clearer customer-centric focus. Lying just below the visible 
surface, the language of innovation permeates operations review. Questions are 
asked differently. Experimentation is an approach in all functions. Projects 
that would have been killed 10 years ago for being too far from the core have a 
much better survival rate today.<br>
<br>
In Whirlpool, the path from ideation to implementation has been successfully 
trailed, both from strategy to implementation and from product ideation to 
implementation. As for strategy, a major win for Whirlpool was the 
implementation of the original design. Pulling 25 people each from three 
different regions, varying in level from VP to line worker, back filling their 
job and having them learn tools and the process of innovation for a year. Still 
more impressive, was that their primary purpose was to learn tools and 
processes, not necessarily to instantly drive product innovation. This was a 
remarkable strategic investment for a company, which the financial market 
scrutinises rigorously.<br>
<br>
<b>Integrated systems<br>
</b>From a product perspective, one of the key inflection points was not nearly as 
sexy as it was critical. Integrating our I-pipe (pipeline of innovation 
projects) with our standard product development process was a critical step in 
Whirlpool’s innovation story. This allowed for one project management system, 
one resource allocation system and one view for top management. At this point, 
innovation projects competed economically with our portfolio of other projects. 
This created a more efficient allocation of resources versus having percentage 
quotas for each type of investment. It also allowed the product and brand 
groups, who were closest to their business, to create the best project portfolio 
for both short and long-term success.<br>
<br>
While there certainly is a tendency for short-term needs of the business to kill 
strategic, long-term investments. Here, it is important for organisations, 
specifically those that are new to innovation and/or not in a traditionally 
“innovative” industry, to protect some funds to ensure an appropriate level of 
innovation investment. This, though, is not the ideal. In a utopian system, 
innovative projects will compete directly with other projects for resources and 
the business will choose the perfect portfolio. Many companies do some of this 
well, but very few do it great.<br>
<br>
Here, it must be remembered that the strength of an idea is relative to the 
needs of the business. Similar to a personal investment portfolio, Whirlpool has 
a diverse portfolio of projects that range from incremental sure-bets to 
bleeding edge technology projects with very high risk-reward. The real strength 
of an idea comes with how aligned it is to your customers and their needs. </font>
