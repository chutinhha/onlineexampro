<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
		</font><b><font face="Verdana" size="4">Raising hi-tech awareness</font></b><font face="Verdana" size="1"><br>
        <b>Hi-tech clusters prove to be a hit even as Knowledge Oasis Muscat gets 
        ready for its next Digital National seminar in June</b><br>
        <br>
        <img border="0" src="info1.jpg" width="300" height="200" align="left" hspace="6" vspace="4">Knowledge Oasis Muscat (KOM) is gearing up for its next Digital Nation 
        Seminar, scheduled for June 10 at the Crowne Plaza Hotel. Sponsored by 
        OER, Ericsson, Microsoft, Hewlett Packard, SAP Arabia, Nawras, Infoline, 
        RealityCG, Omania e-Commerce, Times of Oman, and Al Watan, the first 
        seminar in the newly launched series, Your Mobile: the Next Frontier, 
        was held on March 4 and attracted over 180 delegates. <br>
<br>
        “I thought the first Digital Nation seminar was a huge success,” said 
        Susie Houh of Ericsson, “It attracted a large and diverse audience. I 
        spoke to people from the oil business, media, telcos and venture 
        capital, it was a great mix. In fact, I don’t know of any similar ICT 
        seminar programme running in the Gulf. This is certainly unique to 
        Oman.”<br>
<br>
        The second seminar in the Digital Nation series is entitled: How to Ramp 
        up Internet Usage, and KOM’s Director-General Mohammed Al Maskari is of 
        the opinion that “the quarterly seminars will help raise awareness of 
        ICT across the country. In partnership with our supporters we’ve drawn 
        up an interesting agenda. For example, we’re looking at how to increase 
        Internet penetration and usage in the Sultanate; the power of digital 
        media; how today’s youth is using technology; and the future of mobile 
        telecommunications. These are big picture issues and we want to talk 
        about them.” <br>
<br>
        Another reason behind the Digital Nation series is to highlight the 
        government’s thinking on technology and its intent on raising Oman’s 
        profile as a potential base for hi-tech firms. In brief, they’re talking 
        clusters and with over 60 firms resident in KOM, the Rusayl-based tech 
        park has the beginnings of a critical mass. <br>
<br>
        <b>Economies of scale<br>
        </b>But what is a cluster? In simple terms, it’s a group of industries and 
        organisations that are more competitive when they co-operate. They can 
        be linked by trading, sharing infrastructure, customers, technology or 
        skills. They’re led by businesses, but are supported by universities, 
        research organisations and government. At the end of the day, the aim is 
        to make businesses more profitable. <br>
<br>
        With its origins in the industrial era, the cluster mantra has its roots 
        in the theories of classical economists, such as Alfred Marshall, and, 
        more recently, it’s been amplified through the writings of such top 
        economic thinkers as Charles Sabel, Michael Priore and Michael Porter. 
        As a result, countries, regions and cities across the world are all 
        expending great effort to discover and expanding clusters that will turn 
        their area into the next boomtown. Oman’s no different.<br>
<br>
        “If we look at the US there’s lots of high-tech production, but they 
        aren’t necessarily tech clusters,” claims Al Maskari. What 
        differentiates a tech cluster is its ability to move to a second phase 
        of economic development where entrepreneurs mobilise local labour talent 
        and finance start-ups. We’re seeing this happen in KOM. Many of these 
        firms are formed as spin-offs by employees of existing companies or out 
        of educational institutions in the area. “California’s Silicon Valley 
        has been doing this for years,” claimed Al Maskari. <br>
<br>
        Oman, however, doesn’t have to be at the leading edge to be successful 
        in high-tech. In recent years, the logistical needs of hi-tech service 
        industries have led to tech growth in smaller cities in the US like 
        Tulsa in Oklahoma, which has become a resource for existing tech 
        clusters. Known as the Blue Collar Internet, local government encourages 
        tech growth by developing transportation infrastructure such as roads 
        and airports, higher education programmes and building fibre optic 
        networks. “This is how we’ve approached developing KOM. We’ve 
        constructed hi-tech, Class A office space, initiated a business 
        incubator programme that has mentoring support from firms like Ernst &amp; 
        Young, KPMG, Trowers &amp; Hamlins, Talal Abu Ghazaleh, and Ericsson. We’ve 
        developed ties with academia, installed a fibre optic backbone 
        throughout the Park and, with Ernst &amp; Young, we’re promoting 
        entrepreneurship through our annual TKM–Ernst &amp; Young Big Business Idea 
        Competition.” <br>
<br>
        <b>Retaining Talent<br>
        <img border="0" src="info2.jpg" width="300" height="189" align="right" hspace="6" vspace="4"></b>But with the growth of hi-tech clusters outside the US, how do firms 
        determine in what cluster to locate? “In our experience, we’ve found 
        that our tenants are looking for an area that they believe will 
        experience significant economic development 10-15 years into the future. 
        Access to a talented, multi-lingual labour pool, higher education and 
        real estate prices are also a concern.”<br>
<br>
        A recent development which excites Al Maskari is the return from the 
        overseas of experienced and highly educated Omani entrepreneurs. “We’re 
        seeing young Omanis with education and work experience investing in KOM 
        and starting new firms. This is an important development,” he points 
        out.<br>
<br>
        The critical issue for KOM, according to Al Maskari, lies in finding and 
        retaining talent. Back in the 1980s and 1990s, he recalls, it was 
        relatively easy to lure top engineers, IT personnel and creatives to 
        cities in the region, but now that has changed. “There are parts of the 
        Gulf that are tough places to live,” he explains. “The commutes are long 
        and the cost of housing and office space has soared. It’s getting a lot 
        harder to get a good quality of life. What we’re doing at KOM is 
        offering those firms and individuals a different opportunity–a higher 
        quality of life.” <br>
<br>
        According to the KOM Director-General, economic-development 
        professionals should heed one lesson in particular: “They’re no longer 
        going to be able to hold onto companies because firms in a particular 
        line of business have always clustered in a specific country, region or 
        city. Areas that want to hold onto their current economic supremacy will 
        only do so by improving their quality of life, services and incentives. 
        If you don’t get these three things right, your efforts are likely to 
        prove futile.”
