      <td><font face="Verdana" size="1"><b>How can clusters benefit businesses and Oman’s economy? 
      </b> <br>
        <br>
        • Companies can increase the expertise available to them if they locate 
        amongst a cluster of other firms. <br>
      <br>
        • Companies can draw upon others with complementary skills to bid for 
        large pieces of work which each of the individual firms would have been 
        unable to complete. <br>
      <br>
        • Advantage can be taken of economies of scale by further specialising 
        production within each firm, by joint purchasing of common raw materials 
        to attract bulk discounts or by joint marketing.<br>
&nbsp;<br>
        • Social and other informal links are important and can lead to the 
        creation of new ideas and new businesses. <br>
      <br>
        • Reputations spread quickly within the cluster, enabling finance 
        providers to judge who the good entrepreneurs are and for business 
        people to find who provides good support services.<br>
      <br>
        • The cluster enables an infrastructure of professional, legal, 
        financial, marketing, technical and other specialist services to 
        develop.</font></td>
