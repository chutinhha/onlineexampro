<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
		</font><font face="Verdana" size="4"><b>OER CEO Golf 2007
comes to a grand end <br>
</b></font>
<font face="Verdana" size="1">
<br>
<b>Swinging on the links has become the favourite pastime for corporate heads – and 
not just for networking or busting stress. In keeping with the adage ‘All work 
and no play makes Jack a dull boy’, OER CEO Golf 2007 provided that challenge 
and excitement – and the head honchos of Muscat rose to the occasion 
magnificently for the third annual OER CEO Golf event held at the Ghallah 
Wentworth Golf Club<br>
</b>
<br>
The crème de la crème of corporate Oman included more than 200 captains both 
from corporate Oman and high profile government dignitaries. The packed 
itinerary was a unique blend of business and golf, catering to experienced 
players, amateurs and beginners alike. <br>
<br>
On the big day, Dr. Amer Rawas, Managing Director of Oman Mobile together with 
Eng. Hani Al Zubair, Managing Director of Zubair Automotive, teed-off the event 
formally. Over 70 golfers then played the tournament attempting to swing a 
hole-in-one to win the Dodge Durango and the OER Champions trophy besides the 
Rolex watch and a gamut of attractive prizes. <br>
<br>
The golfers were segregated into two categories – one that played the full 18 
holes while the others opted for the nine-hole Stable Ford format tournament.<br>
<br>
Golfers-to-be, meanwhile, lined up at the exclusive golf clinic being supervised 
by professionals like Brandon D’Souza, Xavier Bonnery and Paul Sharples to get 
to know the finer points of the game. There were no golf widows at the event. 
Spouses and golf aspirants enjoyed multiple rounds of ‘Crazy Golf’ at sunset and 
tested their skills at the mini-golf arena. Next on the surprise list was ‘Glow 
Golf’, with the fairways lit with candles, and glowing golf balls for the 
couples to enjoy a relaxed two-hole round.<br>
<br>
“In view of the wonderful response to the event for a consecutive third year, 
OER CEO Golf has established itself as a must-attend fixture in the business and 
social calendar in Oman” said Alpana Roy, Assistant General Manager, United 
Media Services. “For months now, the corporate world was buzzing with 
anticipation, and March 8 was a befitting climax to the expectations of the 
corporate community,” she averred. The spectacular day was fully complemented 
with a fast-paced musical evening led by Adam and Cathy Cole playing popular 
numbers. A grand dinner and an award ceremony that saw many business leaders 
take home attractive prizes followed the gala evening.<br>
<br>
As Bennet Stephens of Gulf Air put it, “Events like this are very much needed on 
the corporate calendar not just to re-energise ourselves but also to strengthen 
relationships by sharing some fun experience outside the boardroom.” S.K. 
Virmani, CEO, Bahwan Engineering Group, said, “It was not just a golf event but 
a complete show that was very well organised.” The credit for sure goes to the 
team of OER and Applause Events who organised the event.<br>
<br>
Thanking the golfers, sponsors, and the Applause Events’ team for making the 
event a grand success, Sandeep Sehgal, General Manager, United Media Services 
said, “Events like OER CEO Golf are part of our continued drive as OER – Oman’s 
leading business magazine – presents novel opportunities to the corporate 
community to come together, enjoy golf, and share the fun and excitement with 
their peers.” <br>
<br>
On the occasion, the chief guests, His Highness Sayyid Tarik bin Shabib, 
Editor-in-Chief of Oman Economic Review, Dr. Amer Rawas, Managing Director of 
Oman Mobile and Mr. Justin Barnett, Managing Director of Zubair Automotive, 
presented the trophy to the Champion, Sumant Sunder, the Nett Winner of the 
event. He was also presented with a Rolex watch from Mr. Pankaj Khimji, director 
of Khimji Ramdas. The dignitaries also gave Champion Gross Winner Mr. Abdullah 
Mohammad a trophy and a Bose home theatre system from OHI Electronics.<br>
<br>
OER CEO Golf 2007 was presented by Oman Mobile, in association with Dodge and 
partnered by Alliance Housing Bank, while the nation’s leading publications; 
Times of Oman and Al Shabiba supported the event as media partners. <br>
<br>
Other Partners included AXA Insurance as insurance partner, Ruwi Modern Printers 
as print partner and Tiger Sports Marketing lent their sports management 
expertise to the running of the golf tournament. Infoline, DHL, OUA, Al Turki 
Enterprises and Al Madina Financial &amp; Investment Services were support partners 
of the event. <br>
<br>
The exclusive prizes awarded to the winners were sponsored by Photocentre, Salam 
Studio and Stores, ID Design, Muscat Electronics, Capital Stores, Radissan SAS 
Hotel and LG from OTE.</font><p align="right" style="margin: 3px 10px">
