<p align="justify" dir="ltr" style="margin-left: 5; margin-right: 5"><b><br>
<font face="Verdana" size="4">‘Oman-US FTA to be enforced by July’</font></b><font face="Verdana" size="1"><br>
<b>Dr Said Amer Al-Riyami, Economic Expert, Ministry of Commerce &amp; Industry, has 
been criss-crossing the globe representing Oman in various FTA negotiations. In 
an exclusive interview, Akshay Bhatnagar probes on the progress and issues under 
scanner on FTAs involving Oman.</b><br>
<br>
<br>
<b>
<img border="0" src="face1.jpg" width="300" height="268" align="left" hspace="6" vspace="4">What have been the after-effects of the signing of the Oman-US FTA?</b><br>
The Oman-US FTA has not come into force as yet. We have to introduce certain 
legislations in compliance with the agreement. For instance, we have to issue 
new laws related to Intellectual Property Rights (IPR) and tendering. <br>
<br>
<b>When is the Oman-US FTA likely to be enforced?</b><br>
It has taken more time than we had anticipated. Most probably, by summer the FTA 
will be in force…<br>
<br>
<b>Summer is quite long in Oman…</b><br>
We are looking at July, to be more precise. <br>
<br>
<b>As per the Second Arab Business Intelligence Report, Oman is ranked third 
(behind Saudi Arabia and Qatar) in a list of 22 Arab nations that inspire most 
economic confidence. But why don’t we get to see the same confidence in terms of 
investments as we see in countries like UAE, Bahrain, Jordan and Turkey, which 
are getting more FDI? Do you think red tape is a factor?</b><br>
I do agree that Oman is getting lower FDI compared to many Arab nations that are 
placed lower in the economic confidence vis-à-vis the Sultanate. But, our 
transparent and well-regulated economic system makes us one of the most 
liberalised and attractive markets in the Arab world. <br>
<br>
I don’t think we have much red tape. We are definitely disappointed that our 
investment-friendly mechanism has not translated into increase in investments. 
Hopefully, besides Oman’s FTA with US, the other GCC FTAs with markets including 
EU, India, Pakistan, Japan, Singapore, etc., are going to result in increased 
investments as international investors can invest in joint ventures in Oman and 
from here they can export to our partner countries that have huge markets. <br>
<br>
<b>Which industry sectors in Oman are going to benefit from the FTA with the US?</b><br>
The major projects which are coming up in Sohar–petrochemicals, aluminium, 
etc.–are going to benefit a lot as they will have easy access to the vast US 
market. The textile industry that suffered on the exports front after the 
elimination of the quota system will get a new lease of life as in the FTA, we 
have got an important concession of exporting up to 50 million sq. metres of 
textiles into the US market without meeting the rules of origin. <br>
<br>
<b>Talking of petrochemicals, there is speculation that Dow Chemicals’ 
participation in the petrochemical complex at Sohar is under cloud as the MNC 
doesn’t seem to be satisfied with the project’s development. Don’t you think it 
will send wrong signals to the international investors’ community?</b><br>
I’m not aware of Dow Chemicals intending to pull out of Oman. I think what you 
are saying is not true. On the contrary, Dow Chemicals has made significant 
investments in the project already. To my knowledge, all the projects in Sohar 
are on track as per schedule. <br>
<br>
<b>All FTAs come at a certain price. What challenges will be there for Oman to make 
the FTA with the US a reality?</b><br>
I don’t see any major problems coming Oman’s way due to the FTA. On the positive 
side, there will be an increase in trade and investment. It will also improve 
consumer welfare by reduction in prices and consumers getting immediate access 
to a wider choice. <br>
<br>
<b>Moving to GCC-EU FTA, you were recently in Brussels for the negotiations. What’s 
the status on that front?<br>
</b>I can cautiously say that negotiations have been completed. But there are few 
issues that need to be settled. <br>
<br>
<b>… what are these issues that need to be sorted out?</b><br>
There are some minor issues, like we want the EU to open up their energy 
distribution services for GCC countries. On the other hand, EU has some issues 
on MFN (Most Favoured Nation) side that needs to be settled. I’m positive that 
these issues will be resolved around the time we meet in Riyadh between March 31 
and April 4; we are calling it a drafting session. The agreement draft will be 
readied and will go through the legal scrubbing and thereafter given to the 
ministers to decide. <br>
<br>
<b>Recently, EU Trade Commissioner Peter Mandelson said that GCC states must remove 
restrictions on ownership of business and allow foreign companies to hold 
majority stake in GCC companies. How are you addressing this issue?</b><br>
On the equity cap front, many of the countries Mr Mandelson was referring to 
have revised their laws to allow EU companies to hold majority stake in Gulf 
companies. Other countries have also promised to raise the equity cap after a 
transition period. This issue is almost settled. <br>
<br>
<b>Which countries in GCC had problems in raising the equity cap?</b><br>
I would prefer not to take names. <br>
<br>
<b>What is Oman’s stand on this issue?<br>
</b>Oman was the first country among the GCC countries to finalise this issue. EU 
has no more demands from Oman.<br>
<br>
<b>Do you mean to say that companies from the EU can operate in Oman with a 
majority stake in the local holding without any restriction, irrespective of the 
industry sector?</b><br>
One hundred per cent foreign equity is applicable in most of the industry 
sectors as per the scheduled list, but there are certain exemptions. In some 
cases, the foreign cap is restricted to 70 per cent. <br>
<br>
<b>In which sectors the foreign equity majority is not allowed?</b><br>
In the case of Oman, foreign ownership is allowed in all sectors. Up to 70 per 
cent is guaranteed in all sectors. <br>
<br>
<b>In GCC countries, almost 80 per cent businesses are family-owned or controlled. 
How is this move of majority ownership in the hands of foreign companies going 
to impact their businesses?</b><br>
The scenario is changing. If you look at the stock market, the number of listed 
companies is increasing significantly. As of now, the nucleus of business is 
definitely in the hands of families, but more and more businesses are being 
shared with other entities or public now.<br>
<br>
<b>Did Oman take into confidence the top business families before agreeing to the 
terms of FTAs?</b><br>
When we were negotiating on the FTA with the US, we consulted the OCCI and they 
supported us wholeheartedly throughout the process. A number of seminars have 
been held to expound the details of the FTA. It was discussed extensively with 
the private sector, and they are in agreement with the FTA terms.<br>
<br>
<b>What will be the impact of the permission to set up companies with foreign 
ownership on the future of free zones that offer a restriction-free environment 
for foreign businesses? Don’t you see a conflict between the two as free zones 
lose their uniqueness?</b><br>
I think the whole of Oman is a free zone now! That’s good for the business!<br>
<br>
<b>What about the free zones in other Gulf countries, say in the UAE?</b><br>
There was an issue related to the free zones in Dubai. It has been contentious.<br>
<br>
<b>Has it been resolved?</b><br>
It has been discussed in detail and most probably will be resolved soon.<br>
<br>
<b>We believe that GCC wants to see the removal of customs charges levied by the EU 
on aluminium and petrochemicals products from the region. Has this been sorted 
out? </b> <br>
It is true that EU has not approved our demand in this case so far. <br>
<br>
<b>Seventy per cent of GCC exports to the EU are fuels and derivatives as of now. 
With the FTA in place, how will this equation change? </b> <br>
With the elimination of customs duty, the trade between GCC nations and the EU 
is going to increase significantly. We are also going to see increase in 
investments. <br>
<br>
<b>Don’t you think the GCC’s FTAs with different economic blocs and countries on 
the one hand, and bilateral trade agreements by GCC countries on the other hand, 
are conflicting in nature?</b><br>
At the end of the day, it is a matter of creating and maintaining harmony 
between them. If you look at bilateral FTAs by US with Oman and Bahrain, they 
are part of US’ objective to have an FTA with all the Middle-East countries. I 
do agree that there will be problems as all FTAs are not homogenous and 
different agreements have different terms and conditions aimed at the mutual 
benefits of US and the respective Middle East country. How that harmony will be 
maintained between all the agreements is a million-dollar question!<br>
<br>
<b>GCC countries apply a common external tariff of 5 per cent on almost all imports 
from non-GCC countries. However, higher tariffs apply to some specific products 
in certain GCC member countries, for example, dairy products and wheat in Saudi 
Arabia. Isn’t it contrary to GCC’s customs union? </b> <br>
My understanding is that these are temporary arrangements. But, we are looking 
at a common customs tariff by plugging in the gaps. <br>
<br>
<b>The Gulf countries are enjoying higher oil prices and strong economic growth. Is 
there a real need for GCC states to give away primary and fundamental parts of 
their economic framework in order to facilitate these FTAs? </b> <br>
Oil is not going to last forever. There is a definite need to diversify the 
economy for all the Gulf states. Getting into FTAs with different blocs and 
countries and introducing competition to expand markets is the best way to 
diversify the economy. We must remember that open economies have progressed much 
faster on a sustainable basis compared to closed economies. Take the case of 
Korea, Taiwan, Singapore and now India much closer home, that have benefited by 
opening up their economies. I do agree that we cannot do things overnight and 
these have to be done gradually in phases. That’s exactly what we are doing. <br>
<br>
<b>What’s the progress on other FTAs?</b><br>
In the case of Japan, we are going to have another round of discussion in April. 
India and Pakistan are in initial stages. I’m expecting talks on Singapore will 
move faster. We have not started working on the Australia FTA in the real sense.
<br>
<br>
<b>What about China?</b><br>
We have started working on goods-related talks first. The services sector 
discussions will be done in the next stage. <br>
<br>
<b>Has any timeframe been set for the FTA with China?</b><br>
It is difficult to put a timeframe for the FTAs. Take for example, GCC-EU FTA. 
Serious negotiations have been going on for the last six years. For the last 
two-three years, we have been hoping that it is going to be the last year. But 
we are yet to sign and enforce the FTA. So, putting a timeframe is a risky 
business! Take the US FTA as another instance. Initially, the US wanted to have 
an FTA with the region but when it found that GCC-EU is taking so much time, it 
opted for bilateral FTAs. It worked much faster for the US. Bahrain-US FTA is 
already in force. The Oman-US FTA is signed and is expected to be enforced in 
summer as I stated earlier.<br>
<br>
<b>Do you think Oman’s move out of the monetary union has made any impact on 
various FTA talks, especially with the EU?</b><br>
The development has no bearing on FTA talks. Even in the EU, all states do not 
have Euro as the currency as yet. So, there are exceptions in the EU also.<br>
<br>
There has been speculation that GCC countries are going to revalue their 
currencies.<br>
<br>
<b>What’s Oman’s stand on this?</b><br>
Oman is not going to revalue its Riyal as per my knowledge.<br>
<br>
<b>But if other countries within GCC revalue their currencies, it can create rift 
within the region as foreign trade equations might change?</b><br>
In a family, some differences are always there. We should try to settle them in 
a friendly manner. </font></p>
