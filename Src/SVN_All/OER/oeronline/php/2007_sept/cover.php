      <p align="justify"><font face="Verdana" size="1">
<b>Boost to Omanisation<br>
      </b><br>
Being largely labour-intensive, the retail industry has unleashed greater job 
opportunities for the local population by providing employment at all levels. 
The government has implemented legislation for small grocery stores to be owned 
only by Omani nationals. The modern-format retail sector too has been employing 
trained Omani personnel who have benefited from the numerous training programmes 
provided by the government like Sanad, Intilaqa and so on. The role of OCCI in 
this context has been commendable as during the last few years, it has organised 
a number of training programmes for more than 1,000 trainees, many of those have 
been hired by small shops at hypermarkets. These programmes focused on 
encouraging entrepreneurship and equipping them with the latest business 
management techniques and administrative and technical skills. <br>
<br>
Retail players have welcomed the government’s strategy to develop and promote 
local human resources. As Haider Jawad Sultan, Managing Director, Capital Store 
LLC says, “Our Bahja outlet is a role model for Omanisation as all the beauty 
consultants there are Omanis. All the Omanis are trained according to their job 
profile and are looked after well.” Further, more than 60 per cent of Khimji 
Group’s retail division staff is Omani; the Al Safeer Group supports Omanisation 
by conducting and giving training programmes to their Omani staff.</font></td>
