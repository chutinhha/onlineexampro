<p align="justify" style="margin-left: 10px; margin-right: 10px">
<font face="Verdana" size="1">Despite the surge, the retail market in Oman is still short of being described 
as a mature market and the sector is still dominated by small retailers. As Nick 
Pattison, a noted consultant and director of a number of local companies in 
Oman, says, “There is a lack of scale and fragmentation in the retail sector. 
There are too many small outlets, supported by a distribution system that too is 
fragmented. While it may seem good for employment that there are many small 
shops, in practice sales are lost to Dubai and other places because the small 
shops do not have the working capital to buy stock: they have low stock-turns 
and low sales.”<br>
<br>
Moreover, he adds, unlike developed markets where retail majors manage the whole 
chain themselves through a horizontally-integrated channel, right from 
importation, warehousing, distribution, logistics and retailing of the 
merchandise, in Oman this is not the case. Here, a major importer controls to 
varying degrees the supply chain from importation to retail outlet. As a measure 
to boost the retail sector, Pattison suggests that the sector should be 
decoupled from importation and wholesaling and should move into the age of 
specialist retailers, specialist logistics companies and specialist importers.<br>
<br>
Retail industry in Oman is at the crossroads. It has emerged as one of the most 
dynamic and fast paced industries with several players entering the market. But, 
to reach its potential, the rapidly growing retail sector requires significant 
capital, technology and application of best global practices. International 
retailers have invested significantly in the local economy, and in the process, 
they have improved living standards of consumers, suppliers, and employees and 
generated greater quantity and quality of employment. The gap between the majors 
and the “sale of food stuff” shops is widening. This situation has occurred 
before in other parts of the world and some innovative ideas have emerged to 
maximize the reach of the small store. The time for the overhaul of that sector 
of Oman’s retail industry is now. </font></p>
