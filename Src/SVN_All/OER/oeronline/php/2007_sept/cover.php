<p align="justify" style="margin-left: 10px; margin-right: 10px">
<font face="Verdana" size="1"><b>Challenges ahead</b><br>
The organised retail market’s potential growth notwithstanding, certain pitfalls 
also exist. According to the GCC Retail Report by Colliers International 
released recently, about 16.35 million sq. m of GLA are expected to be added to 
the Gulf’s retail sector by 2010, making it the fastest growing in the world. 
While UAE and Saudi Arabia will see the highest increase, contributing 44 per 
cent and 30 per cent respectively of the GLA by the end of the decade, Oman’s 
contribution will be a miniscule one per cent of the GLA. Further, there are 
challenges such as shortage of retail space causing retail property rents to 
rise, very few malls compared to neighbouring countries, and shortage of skilled 
manpower. In fact, staffing issues such as recruitment, training and retention 
of skilled personnel are a major challenge not only in Oman but throughout the 
GCC region.<br>
<br>
Another challenge is that all the major retailers in Oman are foreign players, 
such as Emke Group, Carrefour, Sultan Centre, Al Safeer, etc. Cashing in on 
their brand equity, mega investments, product variety, economies of scale, 
automation, and strong marketing strategies, big-box retail players are putting 
small traditional grocery stores under pressure. The majors’ lack of capital 
constraints, extensive product ranges and modern retail management methods, have 
putsmall grocery-shop owners owners on the backfoot. As Taj Mohammed Al Zadjali, 
owner of Taj Super Market, a traditional grocery store in CBD says, “We’re 
losing out our customers to these hypermarkets. Customers from neighbourhoods 
visit our shops only for petty purchases of FMCG goods, while for bulk buying 
they rush to hypermarkets as they get competitive prices there. Further, we 
don’t have access to huge capital and technology like big retailers, which 
inhibit our expansion plans.”</font></p>
