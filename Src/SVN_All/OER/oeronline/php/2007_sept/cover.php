<p align="justify" style="margin-left: 10px; margin-right: 10px"><font face="Verdana" size="1">
<br>
</font><b><font face="Verdana" size="4">The buck stops here!<br>
</font><font face="Verdana" size="1">&nbsp;</font><font face="Verdana" size="4"><br>
</font></b><font face="Verdana" size="1"><b>Oman’s retail sector is on the verge 
of a higher growth trajectory given the surge in consumption by a swelling 
population. Sunil Kumar Singh does a SWOT analysis of the retail market</b></font></p>
<p align="center" style="margin-left: 10px; margin-right: 10px">
<img border="0" src="cover1.jpg" width="350" height="441"></p>
<p align="justify" style="margin-left: 10px; margin-right: 10px">
<font face="Verdana" size="1">Shopping for groceries has changed a little. More and more families are making 
an outing of it and not because they have no one to watch the kids. Those 
managing the buildings and those promoting their products come together to make 
these new monuments of consumerism, appear less like the mundane fluorescent-lit 
places you used to dread, but more like a combination of the town fairground and 
the bustling town market. The objective is to dazzle the kids and the adults and 
keep them on the premises for as long as possible.<br>
<br>
<img border="0" src="cover3.jpg" width="251" height="167" align="right" hspace="12" vspace="2">Organised retail in Oman, besides benefiting consumers by way of competitive 
product pricing and quality service, is becoming the next boom industry. The 
modern hypermarkets, supermarkets, etc., promoted by international retail 
players in Oman are increasingly becoming the destination point for shoppers as 
well as window-shoppers. The whole concept of shopping has changed because the 
retail format and consumer buying behaviour has changed. There is everything for 
everyone – shopping, entertainment, and food, all of it under one roof. Demand 
is high for food, grocery, non-food, lifestyle/fashion products, apparel, 
accessories, books, music, gifts, appliances and consumer durables, all of which 
are available across a range of modern retail formats such as hypermarkets, 
supermarkets, superstores, forecourt stores, department stores and convenience 
stores.<br>
<br>
As urban Omanis and expats go up the Malthusian pyramid, market analysts predict 
this organised retail growth will continue. Consider this fact, at the end of 
last year, the total organised retail space in Oman covered close to 200,000 sq. 
metres of Gross Leasable Area (GLA), with approximately 68,000 sq. m under 
development and 75,000 sq. m to be added by 2011, according to Retail 
International, a UK based consultancy (See interview: ‘Retailers need to pull up 
their socks’ on page no. 37). Domestic players like Khimji Mart, Capital Store, 
etc., and international heavyweights like Emke Group, Carrefour, Landmark Group, 
are garnering strong sales growth.<br>
<br>
<b>Demand side up<br>
</b>Multiple factors have provided the impetus for the current retailing 
revolution in Oman. These include a sound macroeconomic condition in the 
Sultanate and growing domestic demand, casting a spiralling effect on the growth 
of different sectors including retail. According to the Ministry of National 
Economy, the strong domestic demand is indicated by parameters such as growing 
consumption. As per the Ministry data, both private consumption and government 
and intermediate consumption by companies jumped from 7.5 per cent to 10 per 
cent from 2003 to 2005. Another parameter, as per the Ministry, is growth in the 
private sector workforce comprising both Omanis as well as expatriates that grew 
by 19.4 per cent last year as compared to 2005, which saw only 2.3 per cent 
growth compared to 2004. Further, the size of the expatriate workforce grew at 
an increasing rate through 2006 with a net addition of 85,600 workers, 30 per 
cent of whom are in senior positions commanding higher salaries. The number of 
Omanis working in the private sector also rose last year forming 18.3 per cent 
of the workforce. All these factors mean that there are more high earners in the 
economy with more spending power, pushing up the demand side of the economy.<br>
<br>
As Abdul Razak Ali Issa, Chief Executive, BankMuscat explains, “Oman’s economy 
has been doing well and this economic boom is being witnessed all around. The 
government has awarded several large contracts for infrastructure building that 
has added to the sense of economic well-being in the economy. The buoyancy in 
the local economy has also attracted investors from abroad who are developing 
large residential real estate projects in Oman pumping additional funds into the 
economy. The growth in economic activity and the resultant increase in labour 
force – both expatriates and Omanis–are helping the retail sector in Oman to 
boom. Further, the increase in salaries for government employees has led to 
higher income levels that is aiding the growth of retail sector.”</font></p>
<p align="center" style="margin-left: 10px; margin-right: 10px">
<img border="0" src="cover4.jpg"></p>
<p align="justify" style="margin-left: 10px; margin-right: 10px">
<font face="Verdana" size="1">Rejoins Raman Kaicker, Chief Financial Officer, Bank Sohar, “What we’re seeing 
in Oman is the confluence of three factors – accessibility, affordability and 
availability – that has led to increasing consumption and, consequently, retail 
growth. When goods become affordable and available and the consumer can access 
the market, consumption is bound to go up.”<br>
<br>
<img border="0" src="cover5.jpg" width="250" height="166" align="left" hspace="12" vspace="4">According to Planet Retail, which is the leading provider of intelligence on 
global retail and foodservice industries, the Omani retail market currently has 
the highest level of concentration in the GCC region, and in 2006 the top five 
retailers in Oman (viz. Emke Group, Al Safeer, Spinneys, Al Jadeeda Stores, and 
Carrefour) achieved a market share of 27 per cent, which is expected to reach 
nearly 50 per cent by 2012. This is because Oman is a relatively small market, 
and one that has already received the attention of several key regional players. 
The long and short of it is that growing population, high disposable incomes and 
the wealth effect are getting consumers in Oman to loosen their purse strings. 
This is facilitating the spread of retail chains where shoppers are not only 
getting quality products at lower prices, but also improved shopping standards, 
convenient shopping, and blending of shopping with entertainment. <br>
<br>
More and more regional and international players are entering the modern-format 
retail market, and the rush to set up more retail stores is not just confined to 
Muscat, but has spilled over to the interior as well. This was not the case a 
few years ago when retail players were only concentrated in the capital area. In 
a couple of years, a few big hypermarket/supermarket chains have come up outside 
the cities on the highways. For instance, last year Al Kawther Shopping opened a 
43,000-sq ft hypermarket in the town of Bidaya, some 160 km from Muscat, located 
between Sohar and Barka. Further, in July this year, the Makkah hypermarket was 
inaugurated at Tharmad, halfway between Muscat and Sohar.<br>
<br>
<b>The discerning consumer<br>
</b>Consumerism is the buzzword in Oman today. A surprising number of the 
country’s consumers are accepting international high-street brands, where the 
retail sector has traditionally been dominated by Oman’s equivalent of the 
neighbourhood mom-and-pop stores. The swelling consumer spending testifies to 
that – four years ago in 2003 while total consumer spending in Oman on items 
such as food, grocery, retail stood at RO3,656 million, it jumped to RO5,672 
million last year, a 55 per cent jump (see graph on page 36). Further, while the 
value of net retail sales was RO1,786 million in 2003, it increased to RO2,659 
million last year, a whopping 48 per cent growth.<br>
<br>
An increase of 55 percent in the consumer spending seems impressive but we can’t 
ignore the role of soaring inflation in contributing towards the surge. In 2003, 
the inflation (CPI) rate was just 0.2 per cent. But over the next three years, 
it has kept on climbing steadily. In 2006, it reached 3.2 per cent. Going by the 
recent figures, it has gone as high as 4.7 per cent in June 2007. As Oman 
imports many products from outside, the inflation in Oman is largely ‘imported’ 
in nature. To make the matters worse, the US dollar has continued to become 
weaker which has put additional pressure on the import bill as Omani Riyal is 
pegged to the US dollar.<br>
<br>
Consumption today is expanding from food to non-food items like lifestyle 
products, electronics, gadgets, and so on. So, what is driving this urge to 
splurge? Consumer pull seems to be one of the most important driving factors. 
Today, every shopper is better exposed to a host of marketing and promotional 
messages on his way from home to the market than a few years ago. For instance, 
to generate enough footfall, hypermarkets/supermarkets, etc., are offering 
everything from luxury cars to overseas holidays. They also use price-offs on a 
range of products to trigger buying. For instance, Khimji’s retail division has 
tied up with major daily newspaper to sponsor a Ramadan Quiz and below-the-line 
brand activities.<br>
<br>
If retail giants are upping the ante, mall owners aren’t far behind. Ibrahim Al 
Qasmi, General Manager, Muscat City Centre says, “We adopt a combination of 
operational and marketing strategies. As regards the operations side, we built 
the flyover to connect the mall with the Sultan Qaboos Highway and make the 
customers’ trip more convenient and less time-consuming. We also added shaded 
parking space for more than 1,000 vehicles.. Our marketing strategies continue 
to entice customers with exciting promotions. The combination of both these 
strategies helps us to improve customer service and take customer satisfaction 
to new levels.”<br>
<br>
Further, sound management technique at hypermarkets is also one of the factors 
that is attracting consumers. As HE Al Khonji, Chairman, Oman Chambers of 
Commerce &amp; Industry (OCCI) explains, “The management at many of the hypermarkets 
do their best to provide high quality service that goes beyond the consumers’ 
expectations through introducing and adapting new marketing techniques. The 
trend has led to a remarkable increase in the turnout in hypermarkets.”<br>
<br>
Another vital aspect is growing consumer awareness. Today, many factors 
influence a consumer’s decision. These include advertising, habit, family needs, 
promotions, pricing, merchandising, packaging, and product information. HE Al 
Khonji reasons, “Many consumers are now aware of the forces of supply and demand 
and can easily discover that hypermarkets provide them with high-quality service 
at competitive rates. They’ve also found that such hypermarkets bring most 
international brands under one roof at competitive prices.”</font></p>
