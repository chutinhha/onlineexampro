<p align="justify" dir="ltr" style="margin-left: 5; margin-right: 5"><b>
<font face="Verdana" size="2">&nbsp;</font><font face="Verdana" size="4"><br>
Model businessman</font></b><font face="Verdana" size="1"><br>
<br>
<b>OER profiles MB Holding Company’s chairman HE Mohammed Al-Barwani, a 
microbiologist who made his fortune in the oil and gas business and created an 
Omani multinational company with operations spanning 14 countries<br>
</b><br>
<img border="0" src="person.jpg" width="250" height="253" align="left" hspace="10" vspace="4">Mohammed Al-Barwani has come a long way. Enroute to a level of success, achieved 
by very few businessmen in Oman, he has done very little by the book. In a 
surprising move, after studying microbiology, he opted for a job with Petroleum 
Development Oman in 1976 and when everything was going smoothly for him, he 
opted out of the secured job at PDO in 1986 to run his part-time business on a 
full time basis. <br>
<br>
Unlike many Omani companies that have been content with protected operations in 
the Sultanate alone, he went out of Oman to enter uncharted territories despite 
the threat of cut-throat competition. Though he faltered at times, today he 
stands tall as the proud chairman of MB Holding Company LLC that has interests 
primarily in oil and gas and mining besides investments and financial services. 
In its 25th year, the company is expected to notch up revenue of US$600 million 
– more than the revenue (in 2006) of Shell Oman Marketing Company SAOG, 
Sultanate’s second largest (revenue wise) company listed on Muscat Securities 
Market. In the process, Al-Barwani has raced past many among the old guard with 
his scorching pace and ever growing appetite for business expansion to emerge as 
a role model for Omani youth.<br>
<br>
<b>Initial days</b><br>
Recalling his early days, Al-Barwani says, “After the completion of my 
undergraduate university education I looked around for a job. I opted for PDO 
that offered me the best option. PDO sent me to Holland to attend courses at the 
Shell Training Centre in petroleum engineering and drilling which was very 
different from what I had studied (microbiology). On my return, I worked on oil 
drilling rigs in the desert. Thereafter I worked for PDO in Mina al-Fahal for 
the next two years as a Petrophysicist. Then PDO sent me to the UK for a 
master’s degree in petroleum engineering.” <br>
<br>
Despite regular promotions and attractive salary, not to forget the respect a 
PDO employee commands in the society, he was bogged down by the monotonous 
nature of the job. He quit PDO in 1986 to turn full-time businessman. “I was 
running a small company called MB Trading, that was into construction work and 
maintainence services, as a side business while working at PDO. I was finding it 
difficult to give attention to both simultaneously. Despite making a good start 
MB Trading lost momentum and started making heavy losses. So I left PDO to 
concentrate on my business on a full-time basis.” It was a risky move as his 
company had already accumulated a loss of more than RO110, 000 and most of the 
businesses were closing down in Oman in 1986 due to a recession.<br>
<br>
But Al-Barwani was determined to pursue his entrepreneurial drive, as it was a 
make or break time for him. He changed his strategy. In his deep baritone voice, 
he says, “I changed the name of the company to MB Petroleum (that still stands 
as the flagship company of MB Holding) and got into petroleum related business. 
Initially I was into trading only. I knew drilling bits, chemical and valve 
suppliers and talked to them to appoint me as their sales representative for 
Oman. Over the years, from being a representative I got into providing drilling 
equipment, consultants and oilfield services. One success led to another and we 
started building our business outside Oman. We went to markets like Indonesia, 
China… we bought a company in Hungary that gave us business in Hungary and 
Syria. We entered Germany, Saudi Arabia and Yemen. In our silver jubilee year, 
we now have operations spread over 14 countries.”<br>
<br>
<img border="0" src="personality.jpg" width="350" height="263" align="right" hspace="10" vspace="4">Today his success story appears very easy but he struggled a lot in the initial 
days. “I started as a representative of companies that were supplying products 
and services to PDO. Most of those companies were based outside Oman and needed 
somebody technically competent to represent them in the Sultanate. I had an 
understanding of their product lines and knew what the oil companies in Oman 
needed. The smaller international companies that we represented couldn’t afford 
permanent offices or to have full time managers in Oman, so they appointed us as 
sales representative. For the initial 5-6 years, our business model was only to 
market products and services of the companies that we represented. As our 
business grew, we started to invest into buying equipment, and developed our own 
services.”<br>
<br>
He will never forget the one contract that changed his fortune. With a smile on 
his face, he shares the incident with us: “PDO was importing in bulk a 
particular chemical. I was interested to find a competitive product for it. I 
contacted a friend in UK who was working for a chemical manufacturer. I asked 
him for the chemical. He said why don’t you manufacture it yourself as it is a 
mixture of two or three simple chemicals. He helped me understand what to mix, 
from where to source the ingredients and also helped me with the drawings of a 
small blending plant. We mixed the chemicals and supplied PDO in bulk containers 
for half the price that they were paying. We not only saved money for PDO but 
also made 40 per cent net profit on blending. The blending was done with the 
support of a small team of just two people with little overhead costs. That was 
one of the best businesses I ever had. We kept that business for 4-5 years.” <br>
<br>
<b>March ahead</b><br>
Not one to rest on his laurels, Al-Barwani is busy scripting the future growth 
of his business. Next goal? Reach US$1 billion consolidated sales in the next 
three years. Not impossible but definitely a mean task ahead. How is he going to 
achieve the target? Through diversification? Not necessary. <br>
<br>
He says, “We will continue growing our core business mainly oil, gas and 
minerals exploration and oilfield services. In addition to organic growth, we 
also plan to grow by making international acquisitions. Investments in oil and 
gas and mining are large capital investments and one must carefully put together 
the financial structure along with having a good understanding of the technical 
side of the business; you have to engineer the two things together. The 
technical knowledge must be partnered with a good financial understanding. And 
one thing you should never do, is get carried away and over pay.”<br>
<br>
He is eying business in Europe and Asia to grow at a faster rate as many 
overseas territories such as India and China offer plenty of business 
opportunities due to their large market size. He understands that it may be too 
ambitious for his companies to become leading global players so he is focusing 
his energies in developing companies which will be regional players in oil, gas, 
and mineral exploration and production.<br>
<br>
Isn’t it too risky to concentrate primarily on the oil, gas and mineral sectors 
alone? He disagrees, “Over the years, I’ve looked at many companies in diverse 
business sectors from close quarters as I was a director of a bank. This gave me 
the opportunity to understand and have a view of lot of businesses in different 
industries. I thank god that I’m in oil and gas, mining and investment/financial 
services; where the margins are better than other business.”<br>
<br>
How will he fund the overall business expansion? He answers confidently, 
“Although oil and gas is a cyclical business but it is profitable and generates 
cash which can be re-invested in the business, and one can also borrow by 
securitizing future cash-flow. We are now raising money through a bond that will 
be soon traded on the London Stock Exchange. The road show for promoting this 
bond is planned for the first week of September.” <br>
<br>
<b>Personal endeavour<br>
</b>In his personal capacity, he is busy working on a project to build a 
business park at Azaiba at an estimated cost of US$300 million. The construction 
is expected to start in 2009 and complete in 2011. “The business park will have 
high quality modern corporate offices, up-market retail facilities, a five-star 
luxury corporate hotel and serviced apartments. We have already negotiated with 
a leading hotel chain and are close to signing a management contract appointing 
them as operators. That company will be entering Oman for the first time. 
Besides, we are also looking at other infrastructure projects in Oman,” he 
informs. He is taking the advice of leading global consultants such as Atkins, 
Cluttons and Dubai-based TRI, and others to give shape to his dream project.<br>
<br>
<b>Changing environment</b><br>
With market liberalisation, family-owned businesses are under pressure to go 
public. Is he ready to do so with MB Petroleum? “At the moment I don’t see any 
short-term advantages of going public when the financial markets are so liquid 
and debt is cheap. Also for a family company to dilute 40 per cent ownership 
immediately on the stock market is emotionally difficult. In many markets, a 
company can go public initially at 15 to 20 per cent and later gradually 
increase its floatation as it sees fit. In Oman, this 40 per cent minimum 
requirement is a psychological barrier for family owned business. Two years ago, 
Omantel went public at less than 40 per cent. If Omantel could be allowed why 
not allow family-owned companies?”<br>
<br>
He adds, “There should be one Gulf-wide stock market for Oman, Bahrain, Qatar, 
UAE, Kuwait and Saudi Arabia. Such a market can then finance large public 
companies that can compete globally against the large Western and Japanese 
companies. This phenomenon is now happing in Asia, where Indian and Chinese 
companies financed by their growing capital markets are becoming globally 
active.”<br>
<br>
Does he feel threatened by market liberalisation opening the field for more 
players? “No. The more free or deregulated the market is made, the more business 
opportunities there will be. I’m not threatened at all. We are already competing 
in markets like Germany, China and India. When governments protect companies and 
markets, they make them weak. Their products and services cannot compete at the 
global level. Allow companies to compete and become stronger. When you open your 
market, all these new companies will need services of one kind or another. This 
will generate new economic activity. It might hurt the weaker companies but over 
all there will be positive impact in many other ways.”<br>
<br>
What are his views on the policy of Omanisation? He says, “Omanisation policy is 
great, it’s extremely important in developing and improving the standard of 
living for citizens in Oman and for developing a strong national economy. To 
ensure that Omanization is a success, it’s important to build a close 
partnership between the government and the private sector. The Government’s role 
is to ensure, that the private companies are responsible in providing good and 
safe working conditions, paying salaries on time, and that employee’s right in 
term of compensation, entitlement etc. are fully honoured by the companies, but 
the government should also make it easy for the companies to fire employees if 
they are not productive. Employees should not be protected in a job if they 
don’t perform. One can still fire an Omani if he is incompetent but it is a 
tedious process to do that. The procedure should be made easier to fire 
unproductive employees. People think that a job is a right. A job is not a 
right! If you are productive, you have a job. If you are not, you should be 
fired. Companies are not charities; they have to compete for their own survival. 
We have around 3,000 Omanis working in our various companies. An employee who is 
unproductive kills the company. We need employees who are company makers, not 
killers. The economy of Oman needs successful companies. Successful companies 
are national assets, they provide essential services, provide employment, pay 
taxes and drives the modern economy.”<br>
<br>
Some people think that he is expanding too fast too soon, but history proves 
that those who think big make it to the top.– Akshay Bhatnagar </font></p><div align="center">
