<p align="justify" dir="ltr" style="margin-left: 5; margin-right: 5"><b><br>
</b><font face="Verdana" size="1">&nbsp;</font><b><font face="Verdana" size="4">‘Our 
strategy is to be in all the GCC countries by 2010’<br>
</font></b><font face="Verdana" size="1"><br>
<b>Akshay Bhatnagar caught up with Abdulrazak Ali Issa, Chief Executive of 
BankMuscat to discuss a range of issues from private placement by Dubai 
Financial to the changing dynamics in the banking sector in Oman. Excerpts:</b><br>
<br>
<b>
<img border="0" src="face.jpg" align="left" hspace="10" vspace="6">Why did you opt for Dubai Financial as a strategic investor?</b><br>
We had been looking at raising capital for two reasons. First, to strengthen the 
capital adequacy ratio. Secondly, we needed capital to finance our organic 
growth and expansion plans in the international market. We were studying various 
options and this opportunity came across. If you look at the developments in the 
recent past, UAE has been increasingly looking at Oman for making strategic 
investments, say in tourism or petrochemicals sectors. With this deal, they are 
now in the financial sector as well in a big way. Dubai Financial opted for 
BankMuscat, the leading bank in Oman, in recognition of our strong track record 
and drive for fast growth. <br>
<br>
<b>Which were the other financial instruments that you considered for capital 
expansion?</b><br>
We successfully raised US$163 million in 2005 using the global depository 
receipt (GDR) route. We could have done that again. We could have raised money 
from the domestic market too, as it is flush with high liquidity. But I think 
the marriage will be very fruitful for us, as Dubai Financial has strategic 
interests in many markets and it will open new vistas for us, especially when we 
are looking at making inroads into new markets.<br>
<br>
<b>With Dubai Financial now and Societe Generale being there right from the 
beginning, as also with the recent GDR issue, BankMuscat is moving more into the 
hands of international investors. Does this mean that BankMuscat is not shying 
away from roping in non-Omani institutions to lead the bank in its growth 
endeavour?</b><br>
Our beloved ruler, His Majesty Sultan Qaboos bin Said, has time and again urged 
us to work towards the progressive integration of Oman with both the regional 
and global economies. We see this investment as in keeping with His Majesty’s 
vision. BankMuscat has time and again looked at various path-breaking options 
when it comes to raising capital: our highly successful GDR issue being cases in 
point. Even this time, we considered the various options available to us and 
chose to go this route as we believe it provides our stakeholders with the best 
possible value.<br>
<br>
<b>
<img border="0" src="face1.jpg" width="350" height="233" align="right" hspace="10" vspace="4">Dubai Financial is now the second largest shareholder in BankMuscat. But you 
could have raised more capital by divesting a slightly larger stake than the 15 
per cent? Were you afraid of giving too much to a foreign player?</b><br>
If you look globally, there has been a significant increase in the number of 
cross-border investments. For a strategic investor, it has to be a reasonable 
stake. But we also have to be responsible to our shareholders. Our shareholders 
and the board realise that the 15 per cent equity dilution was the best in the 
current circumstances. Today, investors from the region and outside are looking 
at increasing investment opportunities in Oman and the Omani government is also 
encouraging this development. In such a scenario, I think 15 per cent dilution 
was the right move.<br>
<br>
<b>Dubai Financial has majority stakes in many companies. Are you open to the 
idea of giving more stake to Dubai Financial in the future?</b><br>
It depends. One cannot say anything on this at this stage. Our strategy is not 
to just grow bigger in Oman. We are also simultaneously looking at opportunities 
in the region and other markets. We are looking at partnering with them in our 
overseas expansion.<br>
<br>
<b>Diluting 15 per cent equity at a consideration of RO238.3 million gives a 
valuation of RO1.6 billion to BankMuscat. Do you think it is a fair valuation?</b><br>
If you look at the capital adequacy position, we are now at 11.4 per cent. After 
the private placement, it will be 18.87 per cent. We are expanding and our net 
worth will also keep on expanding.<br>
<br>
<b>You have stated that Dubai Financial Group will nominate two directors on the 
board of BankMuscat. Does it mean that the board will have to be restructured?</b><br>
It is a bit too early to deliberate on such things, as the transaction is still 
subject to regulator and shareholder approval. We will share the details with 
you at an appropriate time.<br>
<br>
<b>What is your strategy of expansion in Oman and overseas?</b><br>
Our strategy is to be in all the GCC countries by 2010. We have a representative 
office in Dubai and have applied for a license for full-fledged banking 
operations. We are hopeful that we will get it this year. We have already opened 
our branch in Saudi Arabia. From one branch, we plan to have a network of 
multiple branches in Saudi Arabia in the coming years. We also intend to enter 
Qatar and Kuwait. We already have a BMI in Bahrain. We have interest in India 
too. We are also looking at opportunities in other emerging markets. But, the 
focus definitely is on the Gulf.<br>
<br>
<b>BankMuscat has become a large player using the mergers and acquisition route. 
Will you continue to be more aggressive in your M&amp;A strategy?</b><br>
Our doors are always open. If we find an opportunity within Oman and outside 
that is in alignment with our business goals, we will definitely go for it. <br>
<br>
<b>Do you see any M&amp;A opportunity currently in Oman?</b><br>
There are no candidates currently. If there are, refer them to us. The Gulf 
market is not very large compared to many other international markets. Here, 
M&amp;As is definitely an important way to grow, as organic growth is a time 
consuming process. We can’t wait for that. We have to create opportunities for 
growth. With increased number of cross-border investments, the opportunities for 
M&amp;As have definitely grown. <br>
<br>
<b>Are you also a candidate for acquisition by a larger bank in the region?</b><br>
We have not seen any cross-border mergers so far in the region. I think there 
are regulatory and other issues involved that have not allowed this to happen so 
far. Eventually, it can happen to anyone.<br>
<br>
<b>Has anyone approached you with an acquisition proposal?</b><br>
No. <br>
<br>
<b>BankMuscat made an aggressive unsuccessful attempt to acquire Alliance 
Housing Bank (AHB). Do you feel that you missed a good opportunity?</b><br>
The acquisition of AHB could have made a significant difference to our mortgage 
line of business. <br>
<br>
<b>Is it a closed chapter for you or are you still open for negotiations?</b><br>
As far as we are concerned, the chapter is closed. We are strengthening our 
mortgage business. We are going to launch a very innovative offer soon. It will 
be the first of its kind in Oman. We are credited with many innovative products 
and offers in Oman and our customers consider us a ‘financial supermarket’ that 
has solutions for all their financial needs.<br>
<br>
<b>How do you keep yourself motivated at the top, as you are a leader by wide 
margin on most of the parameters?</b><br>
Our people are self-motivated. We continue to invest more in technology to offer 
better service and innovative products to our customers. We invest substantially 
in our human element. They are rewarded suitably for their tremendous efforts. 
We encourage our staff to not only develop skills to do their job better but 
also develop strategic thinking and leadership qualities. We have excellent HR 
development and training programmes in place that are developed and conducted by 
our in-house team along with renowned management experts. We give our employees 
opportunities to realise their dreams. In turn, their higher motivation level 
keeps propelling us to greater heights.<br>
<br>
The competition is growing as we see more local and foreign players getting in 
Oman and in the region also; the market intensity is moving up. We can’t rest on 
our laurels. There is a lot to be done and achieve, and we are collectively 
motivated to make it happen. We are investing in all respects to have the best 
systems, processes and people in place. &nbsp;</font></p>
