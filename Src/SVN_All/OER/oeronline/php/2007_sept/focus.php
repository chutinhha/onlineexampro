<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
</font><b><font face="Verdana" size="4">Changing dynamics<br>
</font></b><font face="Verdana" size="1"><br>
<b>The appetite for fast growth has not subsided in BankMuscat, the largest bank 
in Oman. In fact, it has gone up significantly for the bank in its silver 
jubilee year. OER tracks the bank’s growth trajectory and the strategic 
initiatives in 2007</b><br>
<br>
<img border="0" src="focus.jpg" width="350" height="237" align="left" hspace="10" vspace="4">In 
August, BankMuscat created a major wave in the financial sector not just in Oman 
but the entire Gulf region. In the region’s single largest cross-border 
investment in the financial sector, BankMuscat’s board of directors approved the 
sale of a 15 per cent stake to Dubai Financial Group. The total capital to be 
raised through the private placement was RO238.3 million, with BankMuscat 
issuing 161.57 million ordinary equity shares at a price of RO1.475 per share to 
the Dubai Financial Group. The deal will be completed by the end of this year.
<br>
<br>
A market leader, BankMuscat has a knack of surprising even its harshest 
detractors with innovative strategies, taking the bank to the next level. One 
can always say ‘what’s the big deal’ since BankMuscat as the largest bank in 
Oman can always afford to come up with aggressive strategies to decimate the 
not-so-strong competition. But, it must be remembered here that BankMuscat was 
not the market leader from day one. Instead, it was its hunger and enthusiasm to 
grow and become big that has taken it to this pole position. <br>
<br>
Not many among us know that BankMuscat (then known as Bank of Muscat SAOC made a 
modest beginning on April 27, 1982. In the first year, it had total assets of 
just RO16 million and 25 employees. It was the smallest bank in Oman. But since 
this unpretentious start, it has never looked back and has grown from strength 
to strength.<br>
<br>
By a spate of aggressive marketing, mergers and acquisitions, it continued to 
become bigger and bigger in size. It acquired Grindlays Bank Plc’s Salalah 
branch in 1986. In 1987, it took over the Jibroo branch of Oman International 
Bank. In 1989, it acquired the assets and liabilities of Oman Banking 
Corporation. In 1993, Al Bank Al Ahli Al Omani SAOG merged with Bank of Muscat 
SAOC to create Bank Muscat Al Ahli Al Omani SAOG. In 1998, the bank changed its 
name to Bank Muscat International SAOG. It adopted the new logo – that it 
retains even today that is an abstract representation of three khanjar blades in 
dynamic motion, symbolising the meeting of people and minds, the basis of the 
bank’s growth and success. In 1999, it opened a branch in Bangalore, India. The 
same year, it changed its name to become simply BankMuscat SAOG.<br>
<br>
The year 2000 was a watershed for BankMuscat. The Commercial Bank of Oman was 
merged with BankMuscat. The new entity became the largest bank in Oman. That was 
the beginning of its dominance of the Omani banking sector. The total assets of 
BankMuscat increased from RO747 million in 1999 to RO1,337 million. In 2001, the 
Industrial Bank of Oman SAOG was merged with BankMuscat. It also acquired the 
business of Al Ahlia Securities Company SAOC, and then HSBC’s card merchant 
acquiring facilities in Oman. In 2002, it acquired the Bahrain operations of ABN 
Amro to create the Bahrain branch of BankMuscat. In 2003, it made strategic 
investment in Centurion Bank in India (now known as Centurion Bank of Punjab) 
and merged its Bangalore branch with it. It also acquired Citibank’s loan 
portfolio in Oman.<br>
<br>
Moving on, in 2004, BankMuscat further diversified its revenue streams by 
introducing bancassurance in the Sultanate. During the year, it became the first 
bank in the Middle East to become completely ISO:9001 certified. The same year, 
it came very close to finalizing a merger with National Bank of Oman. The deal 
would have made it substantially bigger. It raised US$250 million through a debt 
issue of a 5-year floating rate note. In 2005, its Bahrain operations were 
converted into an independent entity branded as BankMuscat International (BMI). 
BankMuscat became the first bank in GCC region to be listed on the London Stock 
Exchange as a result of its maiden US$163 million issue.<br>
<br>
In 2007, its silver jubilee year, BankMuscat has launched several innovative 
consumer products- the ‘new’ al Mazyona savings scheme with new features, 
including a free personal accident insurance cover and a special ‘loyalty’ prize 
to reward customers with a consistent savings habit. Other new product offerings 
include Baituna home finance: the Baituna suite of products is available for 
purchase and construction of homes for first-time buyers; purchases of second 
homes or retirement homes; and also for purchase of land for residential use at 
selected locations. Financing is offered primarily to Omani nationals, though 
the suite of products also provides financing options for GCC nationals and 
expatriates who wish to seek housing finance for properties in the Sultanate. 
Another new product introduced this year is the ‘Hayatuna’ Family Protection a 
unique insurance product. Through ‘Hayatuna’ Family Protection, BankMuscat aims 
to provide an umbrella of protection to family members of customers who opt for 
this group life insurance product.<br>
<br>
The Bank also launched an innovative ‘win-win’ customer acquisition campaign, 
which offered existing customers the chance to win a Lexus car when their 
friends started a relationship with the bank. Non-customers who chose to start a 
relationship with the bank – across a number of products categories – were also 
eligible for a similar prize. To continue the celebrations, a BankMuscat convoy 
is travelling to various parts of the country to create awareness about the 
bank’s products and services, generate referrals and reach out to customers 
nationwide. To keep the excitement going, BankMuscat is expected to soon launch 
a new 6-in-1 offer under the baituna banner, which will leverage its strengths 
across various areas of banking and also extend the al Mazyona brand to a number 
of savings schemes focussed on different segments of the target audience. 
Several other products from the consumer banking stable are also to be launched 
before the end of the year. This is all in keeping with the bank’s philosophy of 
partnering with the customer across his/her lifecycle as encapsulated in its new 
tagline: ‘With you, always’.<br>
<br>
To lend further momentum to its international operations, earlier this year the 
bank acquired a 43 per cent stake in the Mangal Keshav Group, a leading Indian 
securities house that has a significant retail presence across the country. This 
is expected to allow the bank to substantially tap into the opportunities that 
exist in the booming Indian equities market today. <br>
<br>
The bank also soft-launched its operations in the Kingdom of Saudi Arabia (KSA) 
in April this year. This, too, is expected to gather momentum in the near future 
as the KSA is widely acknowledged to be the largest market across the GCC 
region. The bank intends to grow its network in the kingdom over the next 3-5 
years so as to be able to fully take advantage of the potential there.<br>
<br>
BankMuscat has also recently adopted the ‘Equator Principles’, a set of globally 
recognized, voluntary guidelines established to assess and manage social and 
environmental risk in project financing, especially in the emerging markets. In 
adopting these principles, BankMuscat has become the first bank from across the 
Middle East region to adopt the Equator Principles. The bank will also ensure 
that the projects it finances are developed in a socially responsible manner and 
reflect sound environmental management practices.<br>
<br>
The bank has also joined the United Nations Environment Programme Finance 
Initiative (UNEP FI), which is a unique global partnership between the United 
Nations Environment Programme (UNEP) and the global private financial sector. 
BankMuscat has signed the ‘UNEP Statement by Financial Institutions on the 
Environment and Sustainable Development’. By signing this statement, BankMuscat 
will join an elite group of 160 financial institutions, including ABN Amro 
Holdings N.V, Allianz Dresdner, Bank of America, Barclays, BNP Paribas, 
Citigroup, Commerzbank AG, Credit Suisse, Deutsche Bank, HSBC, Societe Generale, 
Standard Chartered Bank and Bank of Tokyo-Mitusibishi, who are co-signatories to 
the UNEP FI Statements and who work closely with UNEP FI to develop and promote 
linkages between the environment, sustainability and financial performance. </font>
