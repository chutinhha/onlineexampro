		</font><b><font size="4" face="Verdana">Oman tops Deloitte hotel survey</font></b></p>
        <p align="center">
        <img border="0" src="gcc.jpg" width="400" height="249"></p>
        <p align="justify"><font size="1" face="Verdana">The Middle East hotel market continues to enjoy double-digit growth for 
        the fourth consecutive year – with revenue per available room (revPAR) 
        increasing 13.6 per cent – reveal the half-yearly results from the 
        HotelBenchmark survey by Deloitte Touche Tohmatsu (Deloitte). And in the 
        Middle East, Muscat saw the strongest growth, with revPAR up 54.8 per 
        cent to US$164.<br>
        <br>
        The growth in Muscat has been driven predominately by an impressive 44.2 
        per cent increase in average room rates. Hoteliers continue to benefit 
        from the closure of four key hotels for extensive refurbishment due to 
        scheduled upgrades. Also, the June cyclone caused significant 
        infrastructure damage and forced many residents to shift into temporary 
        hotel accommodation.<br>
        <br>
        Said Lorna Clarke, executive director of HotelBenchmark, “as expected, 
        hotel performance across the Middle East has slowed slightly compared to 
        last year but is still in double-digit territory. The level of 
        investment continues to surprise. In the first quarter of 2007, US$2 
        billion was pumped into the hotel sector. Given the continued 
        investment, strong economic performance, and the attention the region 
        commands, it is not hard to see why the Middle East has the highest 
        revPAR in the world.”<br>
        <br>
        During the first six months of 2007, revPAR increased by US$17, fuelled 
        by improvements in average room rates. At US$108, the Middle East 
        continues to boast the highest revPAR for any region – above Asia 
        Pacific and Europe levels of US$97 and US$100, respectively. Behind the 
        figures, however, the markets across the region have their own stories 
        to tell. There is a huge regional disparity in revPAR; with Dubai 
        topping the scales at US$255 compared to Luxor at US$24.<br>
        <br>
        Riyadh took second place after Muscat, with revPAR increasing by 40.3 
        per cent over the period to reach US$170. Again, the performance was 
        largely due to growth in average room rates, which topped US$200 – among 
        the highest in the region. <br>
        <br>
        Another market enjoying a good year was Damascus. The Syrian capital 
        pushed occupancy up 20.7 per cent to 59 per cent, resulting in a 31.8 
        per cent improvement in revPAR. At US$57, this is still relatively low 
        compared to other markets but confirms that Syria’s strategy to promote 
        its unique historical legacy is paying off.<br>
        <br>
        In Egypt, hoteliers have at last started to see performance bounce back 
        with all markets reporting double-digit revPAR growth during the first 
        half, with the Red Sea Resorts leading the growth reporting revPAR 
        increases of over 30 per cent. These have been driven mainly by 
        occupancy, which is now just below 70 per cent.<br>
        <br>
        Average room rates in Egypt do, however, remain among the lowest in the 
        region, making the country a very competitive holiday destination. Egypt 
        is extending its appeal by diversifying its offerings and attracting 
        more tourists looking for city breaks, medical and wellness tourism.<br>
        <br>
        Beirut and Doha were the only markets to report negative growth in 
        revPAR this year. This was not surprising given the tensions between 
        Israel and Hezbollah in 2006, Beirut saw occupancy levels plummet to 
        32.9 per cent, with its revPAR declining to US$39, the lowest for the 
        region.<br>
        <br>
        Doha also saw Hotel revPAR decline by 3.6 per cent, following the peak 
        levels seen in 2006 when the city hosted the Asian Games. Hoteliers also 
        suffered at the hands of new supply following the partial opening of the 
        Ritz Carlton Sharq Village and Spa, and the Movenpick Tower and Suites 
        Doha. In addition, the Ramada Doha increased room count by over 120. 
        Despite this, however, the city still boasts the second highest revPAR 
        in the region after Dubai.<br>
        <br>
        Comments Rob O’Hanlon, tourism, hospitality and leisure partner, 
        Deloitte Middle East, “the hotel industry (in the region) continues to 
        benefit from increased airline capacity, events and new attractions. 
        infrastructure developments and effective marketing campaigns are also 
        all helping to raise the profile of the region worldwide. So, with this 
        in mind, we expect hotels across the region to continue to reap the 
        rewards.”</font></p>
