        <br>
        <b>IDC: Gulf PC market to grow 16% p.a.</b><br>
        The PC market in the Gulf states continued its steep upward course, 
        expanding 34.4 per cent in volume and 31.7 per cent in value terms 
        year-on-year to almost 2.2 million units valued at US$2.40 billion, 
        according to the latest study from IDC. The report further states the 
        expansion was driven by strong demand for notebooks as well as by 
        ongoing and new IT infrastructure deployments in major vertical markets, 
        such as the government, oil and gas, banking and insurance, and 
        telecommunications, and heightened demand from small and medium-size 
        enterprises, including the SOHO segment.<br>
        <br>
        The Gulf market is dominated by international players, with only one of 
        the top ten vendors being a local vendor. The top three vendors were HP, 
        Acer, and Dell, whose combined market share reached almost two-thirds of 
        total shipments.<br>
        <br>
        The UAE and Saudi Arabia continued to make up the largest share of PC 
        shipments in the region, accounting for an over 81 per cent share in 
        2006. The third largest market, Kuwait, had a share that was just 
        one-tenth of the two biggest markets combined.<br>
        <br>
        Notebooks were again the most dynamic market drivers, with shipments 
        rocketing up 61 per cent year-on-year and comprising 57.5 per cent unit 
        share of the market. Desktop shipments also continued to grow in 2006, 
        although at a much slower pace (at less than 10 per cent) and made up 
        40.5 per cent share of the market. Shipments of x86 servers grew 11 per 
        cent year-on-year in 2006, as more SMBs purchased these machines and 
        large and very large businesses renewed their existing server 
        infrastructure as part of regular replacement cycles.<br>
        <br>
        IDC expects the Gulf PC market to expand at an average annual rate of 
        over 16 per cent in volume over the next five years. “Continued economic 
        growth will fuel expansion in the Gulf states PC market,” said Omar 
        Shihab, research manager, IDC MEA. “Uptake of notebook PCs, the rollout 
        of numerous IT infrastructure projects, and PC renewal and upgrade 
        cycles in the corporate and government sectors will also play a role,” 
        he added.<br>
        <br>
        <b>Moody’s GCC economic outlook</b><br>
        The widespread application of Basel II norms, a standard for capital 
        adequacy norms for banks, is having a positive effect on the local 
        banking systems, says Moody’s in its first-half report on the Gulf 
        region. It said this was enabling institutions to look at their balance 
        sheets in a more risk-oriented way, although high liquidity still mutes 
        core-financing drivers for securitisation. The improving economic 
        situation in the Gulf was evident from the A and Aa ratings of the six 
        GCC governments, much higher than their ratings just a few years ago.<br>
        <br>
        The marked improvement in the economic fundamentals of the Gulf 
        countries has mainly been caused by the sustained rise in global 
        hydrocarbon prices over the past five years. Based on current spending 
        patterns, break-even oil prices are around US$35-40 per barrel. Moody’s 
        says that compared to prior oil-related economic booms, oil income is 
        currently being used more on capital investment than on current 
        spending. Most governments are proceeding with structural reforms aimed 
        at boosting the role of the private sector despite the jump in 
        government revenues. Nonetheless, despite improving economics and rapid 
        GDP growth, the regional political environment remains challenging. 
        However, the large net asset positions and low levels of gross debt 
        suggest that it will take a severe and sustained adverse political 
        scenario to cause a government bond default, either in local or foreign 
        currency.<br>
        <br>
        Moody’s also pointed out that individually, Bahrain, Oman and Saudi 
        Arabia have less hydrocarbon reserves per capita and somewhat weaker 
        government balance sheets. As such, there still remains a relative 
        difference in the ability of these governments to service their debt. 
        Moody’s, however, envisages that the size of the rating differential 
        between them will narrow down over time, as their public finances 
        strengthen, while the ratings of the richer ones are increasingly 
        constrained by political and institutional factors. Reflecting these 
        considerations, the outlooks for Bahrain, Oman and Saudi Arabia 
        government bond ratings are positive. <br>
