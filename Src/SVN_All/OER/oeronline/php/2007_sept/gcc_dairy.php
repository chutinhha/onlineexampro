        <b>‘<img border="0" src="gcc12.jpg" width="204" height="136" align="left" hspace="10" vspace="4">Made in Bahrain’ cars by year-end </b><br>
        Lothar Drescher, the general manager of performance car firm RUF-Bahrain, 
        has said the first cars built in Bahrain will roll off the production 
        line before the end of the year. He said the US$20 million RUF-Bahrain 
        manufacturing facility, adjacent to Bahrain International Circuit (BIC), 
        is set to start manufacturing the firm’s high-end vehicles in October. 
        He added that the firm is studying the possibility of shipping cars from 
        Bahrain to the US as it looks to crack the American market. The 
        US-Bahrain Free Trade Agreement (FTA) means the firm could ship cars 
        directly to the US. “I believe Bahrain has a lot of potential as a car 
        manufacturing location. It is geographically well centred in the GCC; 
        there is also a tax advantage – for example in comparison with Germany – 
        which has quite a huge impact. We will get most of the items in what we 
        call ‘knocked down’ state – the engine parts will be in bits and pieces 
        and it will all be assembled here,” he said.<br>
        <br>
        The new RUF-Bahrain Automobile Manufacturing and Assembly Facility is 
        the first facility German carmaker RUF Automobile has built outside of 
        its Pfaffenhausen headquarters. One of its highest profile models, the 
        CTR3, is capable of accelerating from 0-100 km an hour in 3.2 seconds. 
        “Cars are made specifically to order. The CTR3 will cost approximately 
        BD190,000, and we have 10 orders already,” Drescher said. <br>
