        <b>Qatar economic growth may slow down</b><br>
        <img border="0" src="gcc4.jpg" align="left" hspace="10" vspace="4">Qatar’s real economic growth may slow down to 8 per cent this year but 
        is expected to scale nearly 12 per cent in 2008, while average inflation 
        is likely to moderate to 10 per cent and a little over 8 per cent, 
        respectively. Although, Qatar’s inflation was expected to fall, its 
        levels remain the highest among other GCC nations, according to Saudi 
        Arabia’s Al Rajhi Bank.<br>
        <br>
        Qatar’s inflation had peaked to a record high of about 15 per cent in 
        the first quarter after which it moderated to about 13 per cent in the 
        second quarter. “Qatar and the UAE have experienced the highest 
        inflation rates in the Gulf, particularly in housing,” the bank said, 
        adding average residential rentals had increased by 83 per cent in Doha 
        over the last two years against 60 per cent in Dubai. Moreover, rent as 
        a proportion of total household income reached 33 per cent in Qatar and 
        30 per cent in Dubai.</font></p>
