        Abdullah Bin Mohammed Bin Saud Al-Thani, Chairman, Qtel. Qtel operates 
        in 15 countries in the Middle East and Asia-Pacific through its 
        subsidiary and associate companies. The year 2007 began with a number of 
        expansion activities, placing Qtel in the premier league of mobile 
        operators in the Middle East, North Africa and Asia region. Funding from 
