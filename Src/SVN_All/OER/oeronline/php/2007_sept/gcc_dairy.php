        US$4.5 billion for its major acquisitions. These included 51 per cent 
        controlling interest in Wataniya group having operations in six 
        countries, 25 per cent stake in Asia Mobile Holdings for expansion 
        opportunities in the Asia-Pacific region through its strategic alliance 
        with ST Telemedia (STT) and 78 per cent acquisition in ATCO Clearwire 
        Telecom for providing wireless broadband and other telecommunication 
        services in Pakistan and Jordan.<br>
