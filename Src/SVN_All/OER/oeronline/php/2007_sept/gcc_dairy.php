        <b>Earthquake hits Kuwait</b><br>
        A mild earthquake, with a magnitude of 4.3 on the Richter scale, hit 
        Kuwait on August 18 at 1:46 am. Kuwait Institute for Scientific Research 
        (KISR) reported the quake’s epicentre was in the southern Manageesh 
        area. Tremors lasted 90 seconds. The earthquake was the strongest in 
        Western Kuwait but the oil facilities were not affected by it. In the 
        past 15 years, Kuwait has been hit by four quakes, making the latest the 
        fifth one, said astronomer Adel Al-Saadoun of Fintas Observatory Center. 
        However, luckily none of those quakes were strong. In 2005, Kuwaiti 
        earthquake expert Dr. Feryal Borabee had stressed that Kuwait was not 
        totally safe from earthquakes. She had also said that the Al-Manageesh 
        area was the most dangerous area in Kuwait in terms of vulnerability to 
        earthquakes.</font></p>
