<b>High Safety Standards</b><br>
Escape bolsters its safety leadership with a host of standard safety features. 
Under its skin, Escape has one of the most advanced safety packages in the small 
SUV segment. Standard safety content includes: </font>
      <ul>
        <li>
        <p align="justify" style="margin: 3px 10px">
        <font face="Verdana" size="1">AdvanceTrac with RSC (Roll Stability Control). This Ford-exclusive system can 
predict a vehicle’s path, using a sensor to measure oversteer and yaw by 
monitoring the vehicle’s speed, throttle position and steering wheel angle. When 
the system senses wheel slip or the loss of traction, it applies brakes where 
needed to keep the vehicle safely on its intended path. </font></li>
        <li>
        <p align="justify" style="margin: 3px 10px">
        <font face="Verdana" size="1">Ford’s Safety Canopy, side air curtains deploy in certain side-impact 
collisions, or in the event of an impending rollover, helping to protect front 
and second row outboard occupants. The curtains remain inflated for several 
seconds after deployment to enhance protection during an extended crash event. 
The system features “roll-fold” technology that’s designed to slide the air bag 
between the glass and occupants as it inflates if occupants are improperly 
seated or resting their heads against a window. </font></li>
        <li>
        <p align="justify" style="margin: 3px 10px">
        <font face="Verdana" size="1">Seat-mounted side air bags deploy out of the upper portion of the front seats to 
protect the driver and front seat passenger’s upper and lower torso. </font>
        </li>
