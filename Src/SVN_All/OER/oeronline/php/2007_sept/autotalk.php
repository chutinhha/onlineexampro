<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
</font><b><font face="Verdana" size="4">A pleasant ESCAPE<br>
</font></b><font face="Verdana" size="1"><br>
<b>Notwithstanding the popularity of the SUV segment, the general understanding 
is that the smaller the size the lesser the power. Our recent encounter with the 
FORD ESCAPE proved otherwise</b></font><p align="justify" style="margin: 3px 10px">
<font face="Verdana" size="1"><br>
<img border="0" src="auto.jpg" align="left" hspace="10" vspace="4">The Escape helped ignite consumer demand for more fuel-efficient small 
sports-utility vehicles, which almost doubled the world’s small-utility market 
from 800,000 units in 2000 to an estimated 1.5 million in 2006. The combination 
of traditional SUV capability and crossover construction had made the first 
original an instant success. Over a million Escapes have been sold since its 
global introduction in 2001–more than any other small utility vehicle or 
crossover of any size. Six years later, Escape remains among the sales leaders 
of the small-utility segment, a remarkable feat considering the increased 
segment volume. <br>
<br>
<b>Smooth Drive<br>
</b>For those who consider most 4WDs to be oversized and the smaller ones 
lacking in punch, the Escape can provide the appropriate balance. As standard, 
it is equipped with a 3.0-litre V6 engine, which itself is a plus in Oman as 
very few vehicles offer a V6 so far. The power output is an admirable 230bhp 
with 212lb-ft of torque. A summarised version of our drive would be “smooth but 
zippy”. The acceleration is consistent even at speeds above 100kmph. Throughout 
the entire drive, we were most impressed with its handling. The suspension gives 
a firm sports car-like feel. Conventional power steering systems use 
engine-driven hydraulics, which saps power and efficiency from the engine. The 
new EPAS system on the Escape uses an electric motor. This new system eliminates 
the hydraulic noise typical of conventional hydraulic systems and improves fuel 
economy by up to 8 per cent over previous models.<br>
<br>
<b>Tough Exterior</b><br>
Mostly consisting of flat panels and lacking curves that competing models in the 
segment offer, the Escape appears to be a part of Ford’s 4WD family. This is a 
sort of dilemma, as the car appears to look much bigger than it actually is. On 
the positive side, an owner can have all the advantages of a crossover size 
vehicle, while ‘making it look big’. The standard options include 16-inch cast-aluminium 
alloys, a chrome grill and most impressively the side step, which adds to its 
tough attire. Overall the Escape looks like a car that can get the job done on 
road or off road.<br>
<br>
<b>Roomy Interiors</b><br>
Exterior concepts extend seamlessly into the interior. That is, it looks as 
‘roomy’ as the exterior looks ‘big’. The parking brake is engaged with a 
floor-mounted pedal, due to which there is ample space between the seats. The 
door panels give sufficient elbowroom while the audio speakers sit high in the 
front doors delivering better sound clarity. Ford’s efficient use of interior 
space is exemplified with a broad armrest and storage bins on the rear doors. 
For the audio enthusiast, the Escape comes equipped with an auxiliary audio 
input jack on the head-unit. This makes connecting an iPod, laptop or any audio 
device to the system easy and effective in terms of sound quality. As an 
initiative towards saving the environment, the durable and attractive upholstery 
is made from 100 per cent post-industrial materials which otherwise would have 
ended up in landfills. The Escape in its own league is a unique machine. From 
the outside, the car looks simple but it’s in the small details that it is ahead 
of any other. Be its V6 engine or the way it corners, this car is made to do the 
job and not ‘just’ look good.<br>
