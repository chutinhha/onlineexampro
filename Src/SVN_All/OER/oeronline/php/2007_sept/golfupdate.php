<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
</font><b><font face="Verdana" size="4">
ME golf booming</font><font face="Verdana" size="2"><br>
</font></b><font face="Verdana" size="1">
<br>
<b>
<img border="0" src="golf.jpg" width="350" height="234" align="left" hspace="10" vspace="4">New tracks by Woods, Garcia, Norman will compliment an already deep roster of 
fine Emirates golf courses – adding up to one of the fastest growing top-end 
golf destinations. OER takes you for swing</b><br>
<br>
Golf courses in Scotland, Ireland, England and Spain are the ones that are 
usually talked about when it comes to great international golf destinations. 
Golf courses in the Middle East? Not so much. But that’s starting to change. The 
United Arab Emirates has been doing everything it can to transform its coastal 
boomtown of Dubai into a world-class golf destination, and it scored a coup late 
last year when Tiger Woods announced that he planned to design his first 
signature course there.<br>
<br>
That might have been the biggest golf news to hit Dubai, but it’s not the only 
sign of the city’s growing allure. Earlier this year, Sergio Garcia, Pete Dye 
and Greg Norman announced their collaboration on a new course at the Jumeirah 
Golf Estates.<br>
<br>
These tracks will compliment an already deep roster of fine Emirates golf 
courses, including the Emirates Golf Club, home to the PGA European Tour’s Dubai 
Desert Classic. It all adds up to one of the fastest growing top-end golf 
destinations.<br>
<br>
Here’s a bit more about the Dubai and UAE golf scene, present and future.<br>
<br>
<b>Wind at Jumeirah Golf Estates</b>: Work on this big-name collaboration is 
expected to begin soon, with the first golfers due to hit the track in 2009. The 
fourth course to be built at Jumeirah, Wind will be Dubai’s first links-style 
run, featuring uneven fairways, thick rough, small greens and pot bunkers.<br>
<br>
“I don’t know of another project anywhere in the world that has brought together 
three internationally recognised figures to collaborate on one course,” said 
Norman, one of Dubai’s first big golf boosters, when the project was unveiled. 
“Sergio Garcia brings enthusiasm and excitement to this project that is 
contagious, and there is nobody in the world today who knows more about golf 
course design [than Dye], especially links golf.”<br>
<br>
<b>Tiger Woods Dubai</b>: This track will be Woods’ introduction to course 
design, a prospect that had golf pundits tearing up their keyboards within 
minutes of its announcement. Slated for completion in 2009, Tiger Woods Dubai 
will be a full – on golf resort, featuring a golf academy, sprawling clubhouse, 
spa, hotel and other accommodation options. Not many details about the design 
have been released yet – Woods’ website says updates on future projects are 
coming soon – but one should expect significant elevation changes, as this is a 
prominent feature of the selected location.<br>
<br>
<b>Emirates Golf Club</b>: Known as the Desert Miracle, since it debuted in 1989 
as the first all-grass championship golf course in the Persian Gulf region, this 
club hosts the Dubai Desert Classic on its Majlis championship course. The 
par-72 track measures more than 7,100 yards.<br>
<br>
Emirates GC’s relatively new Wadi course packs an equally challenging punch, 
with water playing more of a role than on the Majlis. And, if you are not 
satisfied with your play during the day, you can spend the early-evening hours 
practicing on the club’s floodlit range.<br>
<br>
<b>Nad Al Sheba Club</b>: Temperatures can reach 125 degrees F during the summer 
in Dubai, so this might be a place to play if you don’t want your Middle East 
golf vacation to feature a trip to the hospital for heat stroke-it’s the only 
fully floodlit 18-hole course in the region. A precision course, with deep 
bunkers, water hazards and undulating greens, the par-71 Nad Al Sheba measures 
6,400 yards.<br>
<br>
<b>Abu Dhabi Golf Club by Sheraton</b>: Peter Harradine designed the two courses 
in this neighbouring emirate, National and Garden. Both feature plenty of palms, 
lakes and bunkers to keep things interesting. The Abu Dhabi also has 36 holes of 
putting and chipping greens, practice bunkers and a 350-metre driving range. The 
club’s golf academy offers personalised coaching.</font><p align="right" style="margin: 3px 10px">
