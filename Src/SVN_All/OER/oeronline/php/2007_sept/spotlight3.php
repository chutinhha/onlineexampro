<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
</font><b><font face="Verdana" size="4">Tech Talk<br>
</font></b><font face="Verdana" size="1"><b>Here’s a lowdown on the world of digital devices that are automating offices 
worldwide</b><br>
<br>
<img border="0" src="spot.jpg" align="left" hspace="10" vspace="4">Even though the main idea of digitising an office revolves around document 
management and communications, there is a scope to expand this horizon. Today we 
see a lot of offices moving towards automation using Digital Access Control, 
Audio Visual system Integration, Audio/Video conferencing products among others 
to optimise their efficiency and presentation and communication practices. Take 
a look. They’re not exhaustive but they illustrate solutions to automating work 
processes.<br>
<br>
<b>Multi-function devices<br>
</b>Multi Function Devices or MFDs are office equipment for paper management 
connected to a PC or network that handles two or more of the following 
functions: printing, scanning, copying or faxing. It’s simple. The quality and 
reliability of individual functions in MFDs is now at a level where they 
parallel single-function devices. If you contemplate buying new shared printers 
and copiers there’s every reason to consider buying digital copiers and use them 
for both purposes. If you want to equip your home or small office, a fax-machine 
MFDs is a good choice as it saves space and decreases running costs as all 
output functionality shares the same ink or toner. Note that especially in terms 
of the lower-end MFDs, capacity might not be equal to that of a single-function 
device. For example, if you only need printing, an MFD is probably not the best 
choice as you are spending money on additional features you don’t need. On the 
other hand, an MFD is a great choice for most businesses that need printers, 
copiers, scanners, and probably facsimile. MFDs exist in a wide performance and 
functionality range and most can be categorised in the following:<br>
<br>
<b>Digital copiers</b><br>
In recent years, high-end photocopiers have adopted digital technology, with the 
copier effectively consisting of an integrated scanner and laser printer. This 
design has several advantages, such as automatic image quality enhancement and 
the ability to ‘build jobs’ or scan page images independently of the process of 
printing them. Some digital copiers can function as high-speed scanners; such 
models typically have the ability to send documents via email or make them 
available on a local area network.<br>
<br>
Some low-end copiers also use digital technology, but they tend to consist of a 
standard PC scanner coupled to an inkjet printer, both of which are far slower 
than their counterparts in high-end copiers. However, low-end scanner-inkjets 
can provide colour copying for a far lower cost than a traditional colour 
copier. Combined scanner-printers also often have built-in fax machines. In a 
few years most copiers will be ‘digital’ (scan and print) instead of the 
customary ‘analog’ variety (expose electrostatic drum and copy onto paper).<br>
<br>
<b>Printing/scanning</b><br>
An MFD or multifunctional, all-in-one or mopier, is simply an equipment 
connected to a PC or a network; enables printing, copying and scanning. It’s an 
office machine that includes the following functionality in one physical body, 
so as to have a smaller footprint in a home or small-business setting (the Small 
Office Home Office or SoHo market segment), or to provide better 
documentmanagement/distribution/production in a large-office setting:</font><ul>
  <li>
  <p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1">Printer 
  </font></li>
  <li>
  <p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1">Scanner 
  </font></li>
  <li>
  <p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1">(ISO A4) Copier 
  </font></li>
  <li>
  <p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1">Fax (optional; a fax-less MFP may be upgraded to include a fax card later, using 
a standard port, e.g. USB) </font></li>
  <li>
  <p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1">Media card readers (home units), to directly print digital camera memory cards.
  </font></li>
  <li>
  <p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1">Hard disk (large office units) for document/image storage. 
  </font></li>
</ul>
<p align="justify" style="margin: 3px 10px">
<b><font face="Verdana" size="1">
<img border="0" src="spot1.jpg" width="150" height="225" align="right" hspace="10" vspace="4"></font></b><font face="Verdana" size="1">An MFD can work both as a computer peripheral (using a computer port, like 
ethernet/Wi-Fi-LAN, USB, FireWire, etc.) and autonomously (with no need for the 
computer connection; for example, to copy, fax and so on). Home or SoHo MFDs are 
usually inkjet-based, and thus allow colour printing and copying, but most 
large-office and a small number of relatively expensive SoHo units use a laser 
print engine for high-quality, low cost-per-page output at large volume. Some 
home units have sockets for various memory cards, allowing printing of pictures 
directly from digital cameras, without using a computer in between. Some current 
MFDs cost less than a good quality inkjet printer and a scanner, but cannot 
offer the same print quality, compatibility, and flexibility, and incur a higher 
cost of ownership. In early 2005, the first MFPs equipped with colour laser 
printers became available.<br><br><b>Blackberry</b><br>Blackberry is the most sought-after handheld communications device for mobile 
professionals. The Blackberry’s biggest advantage has always been its e-mail 
prowess. A Blackberry checks your enterprise or ISP mail server for new messages 
multiple times per minute. Whenever a new message is pushed to your Blackberry, 
the device alerts you with a blinking red light. Blackberrys feature built-in 
support for viewing many types of e-mail attachments, including MS Word, Excel, 
and PowerPoint documents; Adobe PDF files; JPEG and other common image files; 
and HTML attachments. Fonts and formatting are usually preserved, and documents 
are compressed to speed up delivery.<br><br><b>Video conferencing </b><br>Video conferencing is about conducting a conference between two or more 
participants at different sites by using computer networks to transmit audio and 
video data. A point-to-point (two-person) video conferencing system works much 
like a video telephone. Each participant has a video camera, microphone, and 
speakers mounted on his or her computer. As the two participants speak to one 
another, their voices are carried over the network and delivered to the other’s 
speakers, and whatever images appear in front of the video camera appear in a 
window on the other participant’s monitor. Videoconferencing will be one of the 
fastest-growing segments of the computer industry in the coming years.<br><br>
<b>Projectors </b><br>Long gone are the film-reel projectors that offices once used to show off 
videos. Instead of heavy tubes, today’s projectors employ compact digital image 
chips, which enable them to achieve weights generally between about 5 and 15 
pounds, and to range in size from a little purse to a small carry-on bag. An 
important distinction among different models of projectors is whether they are 
intended for office or home theatre use. Office projectors tend to be less 
expensive than home theatre models, with less impressive image quality. </font></p>
