        <img border="0" src="bc.jpg" width="175" height="260" align="left" hspace="10" vspace="4">Welcome to a brand new way of selling with ‘No More Cold Calling’. The 
        author of this book is Joanne Black, who runs her own establishment by 
        the same name as the book. She introduces you to an avant-garde world of 
        referral selling as a better and improved means of achieving healthier 
        bottom lines.<br>
        <br>
        So why referral selling? Is it really workable? Doesn’t it entail a 
        feeling of asking for a favour or appearing weak? Hold on, says the 
        author. It’s a new mindset altogether. Referral selling, according to 
        Joanne Black, is a spanking new way of investing in trust and 
        relationships. You save on time because you are able to filter the wheat 
        from the chaff. Referral selling is a proven solution, and the 
        prospective client knows that he will spend wisely by meeting you. So 
        atTRACT new business, in the words of the author, by going through the 
        five-pronged process of Tell, Remind, Audience, Contacts and Touch Base.<br>
        <br>
        Tell is a process of building the first connect with a referral source 
        and informing. The second step is to remind the source of the values and 
        achievements that can be delivered by tying up with the contact. The 
        third step is to describe the ideal contact audience so as to emphasise 
        the profile that you are looking for. So far, so good. Your referral 
        source is convinced of the merits of the case, and is envisioning an 
        ideal contact whom she or he can refer. Now, we come to the tricky part. 
        This is contacts, the fourth step of the process. This involves asking 
        your referral source to get in touch with the reference he or she has 
        given. Often, the referral source may want to just give you the details 
        of the contact and stop there. Voila, this is the moment of reckoning! 
        Unless the referral source goes on to take the trouble of calling up his 
        contact and introducing you, it is not going to work. Finally when talks 
        get underway after getting an introduction, touch base with the contact 
        to express your appreciation and establish a relationship.<br>
        <br>
        Referral selling is thus a function of Attitude, Strategy, 
        Accountability and Process. Have the right attitude and make use of 
        whatever opportunities you get whether you’re at the airport or at an 
        informal meet. All it takes is practice to get a focused idea of how to 
        get referral selling working to the best of your advantage. It can help 
        clinch deals faster, create win-win situations, obtain a loyal network 
        of customers, fetch better margins, reduce the sales cycle and establish 
        loyal and long lasting relationships. <br>
        <br>
        Usher in a new sales climate by changing the meaning of selling. Stop 
        targeting a wider base of customers who may not be willing to build a 
        business connect with you despite the time and resource you’ve invested. 
        Instead, close in on a smaller subset of loyal customers who believe 
        your credibility because they have a reliable source to fall back on. 
        Set your referral wheels rolling today. 
