<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
</font><b><font face="Verdana" size="4">Drop the old hat!<br>
</font><font face="Verdana" size="1">&nbsp;</font><font face="Verdana" size="4"><br>
</font></b><font face="Verdana" size="1"><b>The way companies’ look and function 
is undergoing a sea change, and the key to this change lies in the concept of 
smart office</b><br>
<br>
<img border="0" src="spotlight.jpg" width="350" height="234" align="left" hspace="10" vspace="6">Till just over a decade ago, the only assets administrative personnel had to 
keep track of were company vehicles. But that was before the technological 
revolution hit the world of business. Today, in addition to the company cars, a 
host of other things have moved centre stage that are deemed no less important 
and valuable to the smooth working of a modern, efficient organisation. From the 
companies’ simplistic concerns for keeping their fleet in a proper trim, 
organisations now have their hands full taking deft care and proper management 
of their gizmo-laden and automated offices.<br>
<br>
Times have changed and changed in a big way. Today’s business owners, office 
managers and execs handle and manage more office assets and equipment than ever 
before. The modern corporate office armoury is impressively high-tech, replete 
with gizmos and the office document management and communication are 
increasingly being automated with top-of-the-line digital devices.<br>
<br>
Smart office or office automation is basically the use of electronic tools like 
computers to automate repetitive tasks. It is the replacement of manual 
operations by computerised methods. It refers to integrating clerical tasks such 
as typing, filing and appointment scheduling. Factory automation refers to 
computer-driven assembly lines. The underlying drive towards automation is to 
improve the way information is collected, managed and shared, as well as reduce 
costs and step up productivity. It also involves making work less tedious and 
trimming hours of workload. Office automation does not always mean punch a 
button and do everything from start to finish. Sometimes the job still requires 
some manual steps through the work process. <br>
<br>
Smart office or office automation is the integration of office information 
functions, including word processing, data processing, graphics, desktop 
publishing and e-mail, and its backbone is a LAN, which allows users to transmit 
data, mail and even voice across the network. All office functions, including 
dictation, typing, filing, copying, fax, telex, microfilm and records 
management, telephone and telephone switchboard operations, fall into this 
category.<br>
<br>
<b>How it works?<br>
</b>An automation system is a precisely planned change in a physical or 
administrative task utilising a new process, method or machine that increases 
productivity, quality and profit while providing methodological control and 
analysis. The value of system automation is in its ability to improve 
efficiency, reduce wasted resources associated with rejects or errors, increase 
consistency, quality and customer satisfaction and maximise profit. Utilising an 
automation system can be a simple change like providing production workers 
cordless drills in place of corded drills to allow for greater freedom of 
movement in performing their task. Complex automation systems integrate computer 
hardware and software, robotic equipment, line equipment, shipping processes, 
inventory control and employee training to increase manufacturing efficiency and 
product quality.<br>
<br>
<b>Comp is the core<br>
<img border="0" src="spot6.jpg" align="right" hspace="10" vspace="4"></b>Computer software and hardware play an important role in office automation 
systems. Computers control and manage the automation of both physical tasks and 
analytical tasks. For example, your store personnel currently hand count 
inventory, then enter it into a PC. Give your employees handheld inventory 
scanners and the inventory information is automatically scanned and entered into 
a computer system where sophisticated inventory management software provides 
analysis of factors such as consumer purchase patterns, inventory management 
metrics and vendor reliability. This automation system yields benefits in time 
savings and accuracy and helps you better respond to customer needs — all of 
which can lead to greater profitability. Look around and you will find 
automation systems everywhere. That email system your corporation uses to share 
calendars, schedule meetings and send communications with is an automation 
system. Fast food chains employ integrated automation systems to ensure 
everything from supplies to data collection, right down to fulfilling your 
order, is done quickly and succinctly. <br>
<br>
<b>To do or not to do<br>
</b>Whether or not to automate a task in your business requires careful 
consideration. There are complex variables involved and not every process will 
benefit from automation. One key area to look at is quality. Can you maintain or 
improve the quality of your product with an automation system? Quality is a 
large factor in gaining and retaining customers. Another major factor in your 
automation decision is value. An automation system needs to provide an explicit 
rate of return on investment. Greater process control, reduced rejects rate and 
speed are value factors to think about.<br>
<br>
<b>Engage a boffin<br>
</b>When considering if office automation is worthwhile, the most important 
thing to keep in mind is whether what you want to do will help increase your 
productivity or are you just adding bells and whistles to a programme because it 
looks ‘cool’? Therefore, one of the best ways to begin the automation system 
process is to engage a professional automation integrator, engineer or planner. 
These individuals can help you discover opportunities for automation in your 
business and manage your automation system development process and 
implementation. Together you will define goals, set budgets, assess potential 
risks and benefits, determine production and carrying capacities, determine 
value, and liaise with employees and management concerning the potential 
changes.<br>
<br>
Have a definite goal/plan in mind when contacting a consultant to programme your 
office automation tasks, as adding functionality without a clear purpose toward 
improving productivity is a waste of time and a drain on resources. Secondly, 
the office needs to determine what type of automation tools, if any, it needs. 
Putting it differently, to determine which technologies may benefit an office, a 
careful assessment of the environment is required that includes the physical 
placement of the office; hours of work; tasks that are performed; worker 
management relations; area where people work; financial goals; etc.<br>
