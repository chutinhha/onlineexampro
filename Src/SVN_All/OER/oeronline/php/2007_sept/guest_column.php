<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
</font><b><font face="Verdana" size="4">Rebranding airlines from the Gulf<br>
</font></b><font face="Verdana" size="1"><br>
<b>It’s time to see the writing on the wall, says Vahid Mehrinfar as he dwells 
on the role that Gulf airlines should play in promoting national recognition as 
ambassadors abroad</b><br>
<br>
<img border="0" src="guest.jpg" width="184" height="546" align="left" hspace="10" vspace="4">Just 
the other day, I found myself yet again in an international airport impatiently 
trying to pass time at one of the departure terminals waiting for my flight to a 
business destination. In the midst of reading a very promising book and at the 
same time keeping up with the various flight announcements overhead, I suddenly 
noticed an interesting phenomenon that captivated me. <br>
<br>
Here I was, in one of Europe’s busiest airports, where many carriers from 
different countries came together not only to provide mere transportation, but 
also making a patriotic pitch for their home country! It was here that I 
witnessed a scene that contradicted the nationalistic agenda that most airlines 
are known to propagate. It also offered potential clues about the future of 
airline marketing and branding in the Gulf region.<br>
<br>
One of the airlines from the Gulf was preparing for departure from the adjacent 
gate where the passengers and airport service personnel were slowly 
congregating. As the flight crew arrived, I noticed that none of them could be a 
Gulf national as they did not have the slightest resemblance to the people of 
the region. Having been an active member of the marketing communications 
industry in the Middle East with a long history of international experience, I 
thought to myself that this could hold the key to many unanswered communication 
mysteries and marketing challenges engulfing our business environment.<br>
<br>
Scanning the faces of the passengers, I wondered how they understood and 
perceived this phenomenon of duality–a masquerade of a Gulf airline with an all, 
or nearly all, expatriate crew?! Where was the old time-tested airline concept 
that proclaimed, “I proudly present to you my nation’s face and voice?” What was 
the representation of Gulf culture embodied by this airline? How authentic and 
relevant was the airline’s presentation? Most importantly, how were the 
passengers supposed to understand this presentation? <br>
<br>
I realised that this was simply a case of entrusting national representation to 
a team acting as foster custodians of a country’s heritage through employment.
<br>
<br>
For more than two decades, I have been battling to simplify the concept of 
national branding in the Gulf under one united umbrella. Seeing this scene, at 
one level, it seemed to me that my efforts had been all in vain and I had to 
accept the truth, painful as it may be. Indeed, how could the Gulf’s service 
workforce be united by a common philosophy, if its very constituents were of 
primarily foreign origin. This realisation was relevant to the discussion on the 
role that the Gulf airlines play in promoting national recognition as 
ambassadors abroad.<br>
<br>
<b>National carriers, or not?<br>
<img border="0" src="guest1.jpg" width="350" height="233" align="right" hspace="10" vspace="4"></b>Regional 
airlines are obviously going through an incredibly exciting period of expansion 
and are justifiably concerned about maintaining their competitive status as new 
carriers emerging onto the scene to take advantage of the upswing in demand. In 
this context, the topic of branding continues to be a critical issue for these 
carriers to induce potency in their regional and international marketing 
strategies. A common dilemma that arises is as follows: My brand is there to 
satisfy my national ego or ensure the portrayal of a great airline known for its 
vision, service ability and attitude by which it converts potential to profit? 
Naturally, struggling to maintain a perfect balance between the two has always 
been the endeavour of every national carrier that belongs to or represents a 
nation. How much contribution can nationalistic rhetoric make to the 
marketability of a carrier, and in reciprocity, what sorts of positive or 
adverse effect can a national carrier generate for its nation? These are 
important questions.<br>
<br>
Let us remember that it was not too long ago that the global airline industry 
and the world community were witness to British Airways’ struggle in the same 
balancing act. “How much of my national pride is the instigator to my commercial 
success, or how much of my commercial success is the instigator of my national 
pride?”<br>
<br>
A simple removal of the national flag from the tail of British Airways created a 
massive shift in consumer perception that nearly shook the foundation of the 
British Airways brand and severely questioned the wisdom of those responsible 
for its welfare at the highest level. A quick retrieval and cleverly executed 
strategy of reintroduction of the Union Jack on the tails of the flying machines 
helped gradually to re-establish the perception of the continuous and seamless 
service that British Airways had come to be known for... a status that was 
earned throughout the years of its evolutionary progress.<br>
<br>
<img border="0" src="guest2.jpg" width="350" height="279" align="left" hspace="10" vspace="4">But, 
national symbolisation can only be important if it stands for customer reward 
and business excellence. Any egocentric national self-aggrandisement cannot 
supersede this important business reality. In the Gulf region, however, often 
the national airlines fail to prioritise commercial objectives vis-a-vis 
national pride by erroneously disseminating confusing messages that swing 
relentlessly between trying to fulfil the two separate agendas. Thus the 
consumer is not empowered to grasp any clear understanding of the true value 
proposition on offer. Even patriotic postures do not provide any convincing 
evidence to the authenticity of intent in order to create genuine international 
interest, or even regional and national pride. <br>
<br>
<b>Language of service</b><br>
Arabic hospitality delivered by European or Asian flight attendants can be an 
interesting experience, provided an incredibly astute marketer and branding 
expert sells this package as a worthy new cultural fusion. Equally, it is 
important to ensure a genuine adoption of the cultural ethos by the foreign crew 
in order to convincingly deliver the rendition of Gulf’s hospitality… as 
theatrical as it may be. For the purpose of consumer seduction, one could look 
beyond the cultural discrepancies provided the principal offer remains 
customer-service driven. After all, the language of service is universal, hence 
no emotional attachment to the national sentiment of the Gulf region will 
replace its importance.<br>
<br>
The British flag on the tail fins of British Airways symbolised years of 
quintessential service… professionally packaged quality under the auspices of 
British pride. This great perceptual achievement was not realised overnight. It 
was not the BA identity that created the magic; it was the brand experience that 
imbued the livery with a special aura! So much so that when the livery changed 
and the flag removed temporarily, it disturbed the magical perceptions about the 
airline and sent customer expectations around it into a tailspin!<br>
<br>
<img border="0" src="guest3.jpg" width="350" height="300" align="right" hspace="10" vspace="3">It 
is unquestionable that the value and quality of airline services are ultimately 
the most accountable touch points for formation of consumer perception. This is 
particularly true of the Gulf region, since we happen to accumulate the biggest 
base of customer support from outbound travellers in the region itself – 
enjoying the benefits of ‘closed skies’ policy and a huge number of manually 
skilled workforce that needs to be moved around.<br>
<br>
Fabricating patriotic messages for a Gulf airline is somewhat self-rewarding, as 
it may be irrelevant and dangerous – more so if there is failure to deliver on 
the real battle ground of service… the ultimate gauge for recognition. If an 
airline delivers on performance yardsticks, opinion makers and target audiences 
may be more responsive to the enactment or embodiment of national branding and 
may even overlook the fabricated authenticity of expressions such as that which 
I witnessed at the airport! Only then can we convince the consumers that colour, 
race, language or religion bear no relevance to the purity of acumen, foresight 
and strength of management that is merely interested in providing a superb 
airline service under the auspices of a culture that, as young as it may be, is 
profoundly meaningful with good intentions.<br>
<br>
<b>Symbols of success</b><br>
An airline’s likeability must first be qualified from its core offering, only 
then may we secure the recall either for service or the culture which helps 
define or substantiate graphic expression in branding. Furthermore, we can 
enhance our competitive position and marketing rewards by reflecting 
aspirational intent with cohesive, likeable and artistically relevant symbols on 
fleet tails. It is time to reconsider the almost predatory nature of certain 
icons on some of the Gulf airlines. National birds or animals may be a 
convenient hook to hang airlines on, but these have become so stereotypical that 
they have lost all uniqueness and substance, and contribute little to cultural 
ethos. Perhaps, it is time to reinvent some new symbols of success for our 
region and back it up with meaningful operations that go hand-in-hand to instil 
pride.<br>
<br>
It had been a hard day and as we taxied down the runaway, I realised that there 
was still hope. I watched an aircraft from a global airline regally soaring into 
the blue skies, and I reminded myself that as there are numerous challenges, 
there are also credible globally acceptable success stories. One airline – 
Emirates – seems to have understood the golden rule of branding and practised it 
to perfection, bringing glory to the home nation and the region, thereby 
conquering hearts and minds. Hopefully, the other players will see the writing 
on the wall! </font>
