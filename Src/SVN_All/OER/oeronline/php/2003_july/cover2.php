                    </font><font face="Verdana" size="4" color="#FF0000">Putting 
            People First - </font><b><font face="Verdana" size="2">Achieving the 
            Standard </font></b><font face="Verdana" size="1"><br>
            <br>
            <b>Last month, another two organisations - National Hospitality 
            Institute and the British Embassy in Oman - joined OCIPED as 
            Investors in People. This will lift the profile of the Standard 
            here, but what have these trio of organisations gained from their 
            success?</b><br>
            <br>
            <img border="0" src="Cov-Maqbaool.jpg" align="left" hspace="6" vspace="3" width="200" height="300">Oman is one of the frontrunners in the Gulf for getting an Investors 
            in People (IiP) structure in place, says Guy Thomas of the UK-based 
            International Development Consortium (IDC), the official strategic 
            partner of the national quality standard in the UK dealing with 
            companies abroad. IDC will deliver practitioner training, support in 
            the development of a quality assurance system and a national 
            delivery structure within Oman.<br>
            Six local organisations chosen in an ongoing IiP pilot just over a 
            year ago have given a good idea of how the IiP UK framework could 
            work in an Omani context. The Omani Centre For Investment and Export 
            Development (OCIPED), BankMuscat, National Training Institute (NTI), 
            the British Embassy, the British Council, Oman Refinery Company (ORC) 
            and National Hospitality Institute (NHI) represent very different 
            organisations both from the public and private sector, being both 
            small and large in terms of number of staff, single and multi-sited, 
            young and well-established, and while some have HR specialists on 
            their payroll, others do not. <br>
            During Thomas' visit to the Sultanate last month, breaking news 
            reached him from IiP UK on June 18 about the progress of NHI and the 
            British Embassy in Oman. Like OCIPED a year before them, each had 
            clinched recognition as 'Investors in People'. <br>
            They join a growing number of organisations across the world which 
            demonstrate a commitment to training and development and reap the 
            benefits that improved people development delivers when linked to 
            the organisation's objectives. In fact, Louise Bower, IiP's 
            international development manager at IDC, says that NHI and the 
            British Embassy in Oman will now join over 34,000 global 
            organisations that currently hold IiP status. Of these, she reckons 
            1,000 entities outside of the UK have been lauded as Investors in 
            People. <br>
            Inevitably, the success of OCIPED, NHI and the British Embassy will 
            boost the local profile of IiP in the Sultanate. Those involved with 
            nurturing and fostering the Standard locally are optimistic despite 
            several obstacles that exist such as the absence of professional 
            managers, lack of attention and care generally paid to HRD (human 
            resources development), and the transient nature of expatriate 
            employees in the local labour market. But there are also those who 
            are sceptical that such a scheme can be successfully imported 
            'off-the-shelf' from abroad. <br>
            &quot;IiP UK has adopted a unique approach to demonstrating the benefits 
            of getting involved with its programme, and now we are keen to build 
            a strategic relationship with an appropriate partner as soon as it 
            can to encourage more companies to get involved,&quot; explains Thomas. 
            Effectively, an office of the Omani government could become the 
            spearhead for this, but there are many other ways that the IiP 
            infrastructure could be established locally.<br>
            &quot;Once developed, Investors in People Oman would promote the 
            concepts, frameworks and standards here, and with the support of 
            IDC, it would take on the role of assuring the quality of IiP 
            advisors and assessors,&quot; says Sue Tappin, who has played a leading 
            role in promoting IiP in the Sultanate and has recently been 
            appointed to represent IDC locally.<br>
            Investors in People has an international quality assurance system in 
            place. Organisations that participate in the programme are assured 
            that the assessors and advisors are themselves scrutinised to ensure 
            their competence and that they uphold business ethics, such as 
            confidentiality guaranteeing that strategic plans are kept under 
            wraps.<br>
            &quot;Certainly, Investors in People will boost Oman's economy and allow 
            organisations here to compete in the global economy. The quality 
            standard will encourage other companies to work with you because 
            they know you are serious about your business and a competent 
            workforce,&quot; says Tappin. &quot;And it will also help to attract inward 
            investment and support the employment of the national workforce by 
            making sure they are attaining the necessary skills that business 
            needs to be internationally competitive.&quot; <br>
            <br>
            <b>OCIPED's triumph: A first for the region </b><br>
            OCIPED was recognised as an 'Investor in People' in July of last 
            year. To capture this award was a remarkable feat for OCIPED, as it 
            is the first individual organisation to gain this prestigious 
            international award in the whole of the Middle East and North Africa 
            region. To achieve the Standard, OCIPED had to go through a rigorous 
            external assessment by Investors in People UK. It demonstrated that 
            it is truly an exemplary organisation that develops its entire 
            workforce and promotes Omanisation. <br>
            Given OCIPED's remit to operate in the global marketplace to attract 
            investment projects and promote Omani exports, achieving a 
            recognised international standard is seen as vital to the 
            organisation's success. As a predominantly 'people' business the 
            achievement of its goals is critically dependent on the quality of 
            its staff, their motivation and commitment to the organisation and 
            the training provided for them. <br>
            Every one of the 33 qualified Omani professionals who are working in 
            OCIPED has their performance evaluated and suggestions are made to 
            further improve their achievement. The spirit of teamwork at the 
            organisation is cited as a major reason they triumphed as well as 
            the fact that the organisation functions in a non-bureaucratic way 
            and the senior management is very approachable operating an 
            open-door policy for staff who want to express their suggestions 
            freely.<br>
            <img border="0" src="cov-ociped.jpg" align="right" hspace="5" vspace="5" width="175" height="263">Just because OCIPED has attained the IIP standard, it doesn't mean 
            that the organisation has earned a rest from scrutinising its HR 
            activities. His Highness Faisal Bin Turki Al-Said, head of planning 
            and follow up, OCIPED, explains, &quot;After Investors in People, the 
            organisation has again reviewed at length how effective employees 
            are in their current positions and roles. We are even looking at 
            changing the functional chart to address some more key issues within 
            the organisation.&quot; Such constant an audit of every employee will 
            serve OCIPED well because reaching the standard was not a one-time 
            evaluation. Indeed, every three years, the IiP auditors will examine 
            their system and others who have succeeded in achieving the IiP 
            Standard.<br>
            &quot;If you have a satisfied employee, you will be better able to 
            achieve the organisation's objectives. The Investors in People 
            Standard is not just a logo on your stationery or a plaque on the 
            wall. It is proof that you are committed to continuously enhancing 
            the contribution of your workforce,&quot; he adds. &quot;Therefore, IiP should 
            not be viewed as burden but a challenge that is actually a natural 
            transition that organisations should go through.&quot;<br>
            <br>
            <b>Success for National Hospitality Institute </b><br>
            Renaissance Services (formerly Renaissance Holdings) SAOG made a 
            decision to join the pilot group of Investors in People in the 
            beginning of 2002. National Hospitality Institute (NHI), a local 
            institution providing training to Omanis and expatriates for the 
            tourism and hospitality industry, was chosen to take the lead amidst 
            its affiliate companies.<br>
            &quot;Prior to embarking with Investors in People, we did have HR 
            practices in place, but I don't think they were particularly 
            focused, being more geared towards administrative HR practices,&quot; 
            says Robert Maclean, Principal, NHI.<br>
            NHI has had a vision statement designed by their employees and every 
            time a new member of staff joins, they must read it, understand it 
            and sign up to it. &quot;It isn't called Investor in People by chance - 
            'investor' is a very important word. IiP has helped us to 
            communicate to every individual staff member the actual contribution 
            they make towards the overall objective of the company,&quot; he says.
            <br>
            But how did NHI prepare for IiP? Well, first it set up a voluntary 
            IiP working committee. A memo was sent out giving a background of 
            IiP, inviting staff to voluntarily join it. From that initial 
            communication, the institute had a committee of eight members which 
            met on a regular basis. After the first assessment from IiP UK, the 
            committee assessed the problem areas that were highlighted. Based on 
            that, they set up a new appraisal system. &quot;We developed a much more 
            thorough and focused training and development plan which tackled 
            detail issues like what training each staff member needed, why this 
            was needed, how is it going to be carried out, who's going to carry 
            it out, what cost will need to be borne, how do we measure its 
            success, what's the desired outcome, and what do we expect from that 
            trainee afterwards,&quot; explains Maclean. <br>
            In the UK, there are underpinning employment laws such as 
            legislation on equal opportunity, and minimum pay. So, there, IiP is 
            built on a bedrock of legislation. In Oman, however, there are 
            labour laws available, but a lot of these issues are still grey 
            areas. There is blatant inequality in terms of the salary structures 
            between different nationalities in all the economies of the Gulf. 
            And if a company here decided to pay everyone an equal salary, that 
            firm would inevitably go bust quickly probably not having enough 
            money to fly its expatriate staff home. So how do you deal with that 
            scenario if you are going to set up a system of quality HRD, a 
            fundamental aspect of IiP? <br>
            &quot;What NHI has done is, in the field of training and development, 
            treat everybody as equal. The inequality in the salary structure is 
            not what Investors in People is all about,&quot; he adds. In fact, even 
            in the UK, people working in London have different salaries doing 
            exactly the same kind of job as somebody working in, say, the north 
            of England. &quot;IiP is very concerned about the development of people. 
            If you look at the IiP structure, it doesn't talk about 
            remuneration. It talks about rewards - and rewards are what people 
            value.&quot; <br>
            <br>
            <b>British Embassy gains its award </b><br>
            One wouldn't expect a UK government mission like the British Embassy 
            in Oman to have many difficulties with completing the IiP programme 
            and gaining accreditation - but it did. One of these problems was 
            that unlike a business, it's not always so easy to measure a 
            government organisation's success. &quot;There is no balance sheet, no 
            profit share, no black and white figure on how you have performed,&quot; 
            explains a spokesman at the Embassy. &quot;The measurement of success 
            against objectives can be more subjective, and you have to work 
            harder at making sure everyone is working for a common cause when 
            your overall functions may be very different.&quot; <br>
            Many government departments in Oman will also face similar hurdles 
            if they move towards embracing the Standard. In an Omani private 
            firm, everyone's work can affect profitability directly. But, for 
            example, at the British Embassy, how some diplomatic tasks might 
            affect procedures is hard to determine. &quot;We have tried to address 
            this partially by putting people from one section to job 'shadow' a 
            colleague in another from time to time, in order to increase their 
            understanding of the Embassy's different roles among staff,&quot; reveals 
            the spokesman. <br>
            What many organisations would want to know is what procedures does 
            it need to enact to successfully go through the IiP programme. &quot;It 
            can be difficult to persuade people that something which, initially 
            at least, seems like just an abstract idea, can actually benefit 
            them in their work. But in terms of formally meeting the 
            requirements, one of the things that stands out is the way personal 
            objectives are set and understood,&quot; he adds. It is very important 
            that an individual has clear objectives and goals, and that these be 
            derived from the organisation's overall objectives. Every member of 
            staff needs to know what he or she is doing and why, and understand 
            how everyone fits in the organisation. It sounds obvious, but you 
            cannot co-operate properly with your colleagues if you don't know 
            what it is they're trying to achieve. </font>
