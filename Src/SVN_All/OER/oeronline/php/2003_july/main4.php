            <br>
            <b>If aesthetics and attitude count, the Cadillac CTS could serve 
            the needs of many. Nayl D'Souza finds out what it has to offer </b>
            <br>
            <br>
            <img border="0" src="car_review.jpg" align="left" hspace="5" vspace="5" width="150" height="189">Designed to spark global interest from the masses and set to muffle 
            sceptics in Europe, the US$4 billion remake of the product line of 
            Cadillac - the flagship brand of US parent company General Motors 
            (GM) - has begun with the roll-out of the CTS. It's a four-door, 
            mid-size sports sedan which is intended to woo customers away from 
            the likes of the Audi A6, the Mercedes C320, the Lexus ES300, and 
            the BMW 330i. The 3.2-litre model doesn't do a bad job of competing 
            against them. Following the failures of Cadillac to re-engineer its 
            image at the end of the last century, perhaps the name CTS implies 
            it is 'chosen to succeed'. <br>
            Firstly, unless you want to blend into the crowd, the CTS scores 
            highly because there are so few people driving it around in the 
            Sultanate. Secondly, there isn't any car being mass-produced that 
            resembles it on the planet anyway. Thirdly, it is very hip. This has 
            got lots to do with its starring role in the Hollywood blockbuster 
            Matrix Reloaded, a fact definitely not lost on Oman's Cadillac 
            dealership which is spending a considerable amount of effort 
            publicising it in and around a well-known local cinema among other 
            locations and mediums. Meanwhile, GM has pitched in by universally 
            tying-up with the Inter-Continental hotel and resort group to 
            promote the CTS further. <br>
            So we've established that this is a well-marketed, trendy 
            US-manufactured luxury car which is rarely seen in Muscat. One would 
            imagine, therefore, that in Oman its price reflected this. Think 
            again. The Cadillac CTS (automatic transmission) starts from only 
            RO10,900 onwards. Here, the base model is fitted with a 2.6-litre 
            engine, fabric seats, a decent CD player, and 16-inch alloy wheels.
            <br>
            The CTS OER tested was the top option in the 2.6-litre model range 
            and came with leather seats, a Bose 6-CD changer audio system, 
            17-inch alloy wheels, as well as an automatic sunroof, high 
            intensity headlamps and some neat touches of wood interior trim on 
            the steering wheel, gear shift lever and inside door handles. <br>
            However, one must consider parting with more cash and opting instead 
            for the top-of-the-range CTS 3.2-litre model because it has far more 
            superior power, as one would expect. It produces 220 hp at 6,000 
            rpm, 218 lb-ft at 3,400 rpm and accelerates from nought to 100 km/h 
            in 7.3 seconds. <br>
            For the rest of this review, we mostly describe what you get when 
            you part with the least amount of money for your CTS. <br>
            <br>
            <b>Outdoors <br>
            </b>The front of the CTS has a V-shaped visage that wouldn't go 
            amiss on a mini-snow plough. In fact, this car is without doubt the 
            most menacing looking saloon you can buy in the Sultanate. During a 
            road test, the CTS got its fair share of interested glances with a 
            few ostensibly turning to apprehension. <br>
            Drawing much influence from some popular prototypes such as the 
            Vizon and the Evoq, Cadillac has obviously designed this car to 
            appeal to affluent individuals who are youthful, confident and image 
            conscious. To some its long, slender appearance is stylish and 
            simple. To others it is less sleek, perhaps even ugly. The 
            sophisticated angled lines on its chassis, its large louvered 
            grille, its jagged edges, and sophisticated vertical headlamps are 
            all evidence that this car is cutting edge (yes, an intended pun). 
            There's little time to ogle its chrome door handles, body-coloured 
            bumpers, and tinted glass but they're worth a mention too. If you 
            decide to choose the CTS in sterling silver as was the colour test 
            driven by OER, these features help to make it look as if it was 
            chiselled out from a block of steel. Like 'sable black' or 
            'cashmere', silver is available free of cost but you can also choose 
            'white diamond' for an extra RO300. <br>
            <br>
            <b>Indoors </b><br>
            Gaining entry to the CTS is easy with its keyless remote entry 
            system. From the moment you climb inside, you'll notice the styling 
            is as futuristic as the exterior. Most of the amenities you would 
            expect from a competing luxury car are present. Power door locks, 
            seats, windows, mirrors, and trunk release are available. <br>
            The driver's information centre is remarkable clever. The 
            computerised stuff is a little baffling at first. For those unable 
            to spend hours reading the handbook, trying to figure it out by 
            trial and error is difficult and suicidal if you are driving at the 
            same time. Nevertheless, an array of personalisation features lets 
            you program your preferences for seating, mirror settings, climate 
            control, audio, sound system, and security settings. The buttons and 
            dials on the centre console feel very durable while the steering 
            wheel is comfortable and rather than throwing a zillion-and-one 
            wheel-mounted knobs, Caddy gift you with just four configurable 
            buttons. All of the hi-tech fare combines to offer a surprising 
            amount of fun behind the wheel. <br>
            There is extensive comfort inside for all occupants although, as one 
            would expect, the most attention to convenience and luxury is 
            afforded for the driver. Leather seats (in options above the base 
            model) come with eight-way power adjustment and memory settings for 
            the driver's seat, rear view mirrors and some other parameters. <br>
            Also, there are seat warmers for driver and front passenger which 
            are, of course, totally useless in Oman unless you are a some kind 
            of masochist. Undeniably, the CTS does seem to cater more towards 
            people living in more temperate climates rather than scorching humid 
            ones. The dual zone climate control is, however, more than adequate 
            to cool the car here and although the seat warmers are wasted, the 
            winter mode (which gives the car more grip and better handling on 
            the road, particularly in snowy or icy conditions) could at least be 
            used if you were planning on, say, taking the car on gentle off-road 
            excursions or on the beach. And there's a fairly large trunk if you 
            are planning a picnic.<br>
            <br>
            <b>Performance </b><br>
            The 2.6-litre CTS has a V6 engine which emits a really pleasing 
            growl when you squeeze the gas pedal and produces 185 hp at 6,000 
            rpm and 180 lb-ft of torque at 3,400 rpm. It reaches a top speed of 
            223 km/h. Key features of the longitudinally-mounted V6 engine are 
            its aluminium cylinder heads, four-valve technology and exhaust gas 
            re-circulation.<br>
            Helping to make the CTS nimble is a front suspension that features 
            independent, short/long arm, coil-over shocks, with anti-roll bar. 
            The rear consists of independent, modified multi-link, coil springs, 
            automatic load levelling shock absorbers, with an anti-roll bar. 
            Truth be told, the rear-wheel drive configured roadster hugs tight 
            corners exceptionally well and is an exceedingly quiet car on the 
            highway, particularly when you're changing gears at low speeds. The 
            shake, rattle, and roll is even absent when travelling across lane 
            studs which greet you in abundance around Muscat's Corniche. <br>
            Fuel efficiency remains an enigma. Details on how economical the CTS 
            is are especially sketchy for the 2.6-litre model but there are some 
            indications from independent car magazines in Europe which suggest 
            it does well in this field. <br>
            <br>
            <b>Safety </b><br>
            The CTS has six dual-stage air bags fitted as standard and 
            three-point safety belts integrated into the rear seat. <br>
            The car's responsive braking is confidence inspiring. Stopping power 
            comes from four-wheel power assisted discs with high performance 
            brake linings, ABS and a focus on traction control and stability. 
            The standard traction control helps retains the car's forward 
            momentum in low traction conditions while its 'StabilitiTrak 2.0' 
            enhances driver control by helping to keep it on the driver's 
            intended path (correcting significant understeer and oversteer 
            should they occur). <br>
            <br>
            <b>The road ahead<br>
            </b>A spokesperson from GM recently told dealers in the Middle East 
            that the company will pump US$200 million to sustain its upswing in 
            the region following success which has resulted in a doubling of its 
            sales to 50,000 units in the past two years. Dealers in the Gulf are 
            excited. In 2003, there are four or five brand new models for their 
            customers to look forward to including the introduction of the new 
            Escalade EXT (a luxury pick up), the Escalade ESV (which is set to 
            become the most powerful locally available SUV), the XLR (a sports 
            coupe), the SRX (a medium luxury sedan) as well as a sports edition 
            of the CTS. The word on the street is that Cadillac is even 
            preparing to release an ultra-high performance CTS-V in 2004 with a 
            5.7-liter V8 engine cranking out around 350 hp but this may be 
            difficult to lay your hands on here. With so many treats around the 
            corner, Caddy should maybe begin castings for some other major 
            movies should they require the extra publicity.<br>
