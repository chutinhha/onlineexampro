            <br>
            It is well-established in Oman corporate law, and its supplementary 
            regulatory scheme, that a person who acquires special knowledge or 
            information by virtue of a confidential or fiduciary relationship 
            with a public joint stock company (&quot;SAOG&quot;) is prohibited from 
            exploiting that knowledge or information for his own personal 
            benefit. This general principle is especially apposite to the 
            conduct of directors of SAOGs, whose broader fiduciary duties of 
            care and loyalty were addressed respectively in the May and June 
            issues of Oman Economic Review. <br>
            <br>
            <b>The legal regime on insider trading </b><br>
            The trading of securities by those individuals who acquire material 
            information about an SAOG as a result of their confidential or 
            fiduciary relationships with it (i.e. corporate &quot;insiders&quot;), is 
            primarily governed by Article 107 of the Oman Commercial Companies 
            Law. This provides that &quot;no member of the board of directors nor any 
            senior employee may take advantage of any information reaching him 
            by virtue of his office or employment in order to secure a benefit 
            to himself...&quot;. Article 107 has been supplemented by the Capital 
            Market Authority's (&quot;CMA&quot;) Circular 29/2001 which re-iterates the 
            restriction on directors (and other insiders) imposed by Article 
            107, while also imposing a duty on directors to disclose material 
            information to shareholders and the investing public. The more 
            recently issued Ministerial Decision 137/2002 establishes the joint 
            and several liability of directors who engage in insider trading and 
            who fail to implement the required disclosure policies, with the 
            possibility of criminal liability as well. <br>
            The essence of these legal rules is that a director in possession of 
            valuable, non-public, inside information entrusted to him in 
            confidence must either disclose it to the public in accordance with 
            the procedures provided for by the CMA or, if he is restrained from 
            disclosing it for any reason, abstain from trading in (and advising 
            any other investor with respect to) the securities concerned while 
            such inside information remains undisclosed. If there is any 
            violation of these obligations, the board of directors may face 
            civil and, in certain cases, criminal liability to shareholders, the 
            company and any affected third parties. <br>
            <br>
            <b>Materiality of information and disclosure</b><br>
            A director is not, of course, always prevented from investing in the 
            company he serves merely because he may be more familiar with 
            company operations than outside investors. A director's duty to 
            disclose information, and his duty to abstain from dealing in his 
            company's securities before such information is disclosed, arises 
            only in any situation which would be reasonably certain to have a 
            substantial effect on the market price of the security if it was to 
            be disclosed, or if it would otherwise affect an investor's decision 
            to &quot;buy, sell or hold&quot; the securities. Circular 29/2001 defines 
            &quot;material information&quot; as information which, if disclosed, &quot;is 
            likely to have an impact on the price of the company's securities, 
            or [which] reasonable investors would want to know...before making 
            an investment decision.&quot; The effect of this is that a director will 
            therefore be under a duty to disclose information (and abstain from 
            dealing until such information is disclosed) if a reasonable 
            investor would attach importance to the information (including any 
            fact which, in reasonable and objective contemplation, might affect 
            the value of the SAOG's shares) in determining his choice of action 
            with respect to any share transaction. <br>
            A director's obligation to disclose is not so broad so as to confer 
            upon outside investors the benefit of any superior financial 
            knowledge or other expertise by disclosing his educated guesses or 
            predictions. The only legal objective is that access to material 
            information be enjoyed equally among all investors, but this 
            objective requires nothing more than the disclosure of such material 
            information so that outside investors may make their own evaluations 
            and draw their own investment conclusions with knowledge equal to 
            that of the company's insiders. <br>
            <br>
            <b>When may a director trade? </b><br>
            Although there are no prescriptions in the legal regime about the 
            length of time a director must refrain from trading after disclosure 
            of material information is made to the public, it is generally 
            understood that if directors are to satisfy their legal obligations 
            they must wait until such information has been effectively disclosed 
            to the public. For example, it would be unreasonable to think that 
            directors may seek to &quot;beat the news&quot; by immediately trading the 
            securities after the release of the material information to the 
            media. They should wait until all of the legal and practical 
            procedures for public release of the information have been carried 
            out and disseminated into the market. <br>
            <br>
            <b>Conclusion </b><br>
            There is a major difference between corporate officers who assume 
            the same risks and obtain the same benefits as other shareholders, 
            and those who use their privileged position to gain special 
            advantages not available to others. Although the SAOG may have 
            little interest in the day-to-day dealing in its shares, it has a 
            great interest in maintaining a reputation of integrity for its 
            management and in ensuring the continued public acceptance and 
            marketability of its stock. When directors of SAOGs attempt to gain 
            personal profits by insider trading, the effect may be not only to 
            distort the true value of its shares, but also to cast a cloud on 
            the SAOG's name, injure shareholder relations and undermine public 
            regard for the SAOG's securities. </font>
