            The bank has set itself strategic targets for the financial year 
            2003, and among the ones at the top of the list is human resource 
            development. Apart from preparing documents for systems and 
            processes for every department in the bank, Carlough has attaining a 
            HR certification on his agenda. &quot;The bank is devoting the first half 
            of the year to customer service training by product/process experts 
            and also training to managers for service delivery management,&quot; he 
            says. &quot;The second half of the year will be dedicated to sales 
            management.&quot;<br>
            The bank, in collaboration with the Arab Academy for Banking and 
            Finance Sciences, is undertaking staff development programmes, 
            training 900 of its employees under various training schemes. The 
            training programmes, which began in the last quarter of 2002, will 
            continue till 2004, and will focus on providing service quality to 
            the customer. <br>
