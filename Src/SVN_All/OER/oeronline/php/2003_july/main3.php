            <img border="0" src="cor-rep-oib3.jpg" align="left" hspace="5" vspace="5" width="175" height="258">The year 2002 was a dream year for the &quot;people's bank&quot;, as chief 
            executive John Carlough likes to refer to Oman International Bank (OIB). 
            After all, it isn't often that a two-decade-old institution 
            experiences a breathtaking 839.27 per cent growth in profitability 
            and an 848.39 per cent jump in Earnings Per Share (EPS). <br>
            Why was it then that the Sultanate's first Omani-owned commercial 
            bank witnessed a 30 per cent decline in operating profits for the 
            first quarter of the current year - RO4.34 million in the previous 
            year to RO3.35 million this year? &quot;This is largely the result of the 
            deliberate decision by the bank to focus on its asset quality, 
            reducing the loan book by 8.4 per cent,&quot; informs Carlough. <br>
            But the result on the bank's P&amp;L Account is still negative, isn't 
            it? Disagrees Carlough: &quot;Our strategy to improve the asset quality 
            of the bank in the past few years has had a very positive impact on 
            the provision for loan impairment, which showed a substantial 
            reduction of 62.6 per cent to RO5.77 million for the year 2002 as 
            against RO15.437 million for the year 2001.&quot;<br>
            <br>
            <b>Frugal financing </b><br>
            As Carlough would have you believe, it is about fundamentals. &quot;We at 
            OIB believe in long-term gains at the cost of short-term pain, and 
            the loan-book clean-up exercise undertaken by the bank is a step in 
            that direction,&quot; he avers. Little wonder then that the bank's 2003 
            strategy entails a re-look at industry benchmarks and an intentional 
            attempt at building up the low-cost savings deposits.<br>
            A major part of that plan to buttress the savings deposits is the 
            Mandoos Scheme, which has led to a sizeable surge in the bank's 
            deposits since its launch. &quot;We have achieved an increase in our 
            marketshare since the launch of the Mandoos Scheme - from 29.45 per 
            cent in December 2002 to 31.51 per cent in May 2003. In fact, we now 
            have a special taskforce to manage the affairs of the scheme, which 
            offers the largest ever prize money in raffle draws in the 
            Sultanate,&quot; says Carlough. <br>
            The bank has recently revamped its Mandoos and Masroor schemes to 
            make them &quot;more exciting and interesting.&quot; OIB's Masroor Scheme 
            squarely targets the Sultanate's youth and, according to Carlough, 
            is in line with the bank's philosophy of focusing on future. &quot;The 
            Masroor Scheme will help us build our future base.&quot;<br>
            On the corporate side too, Carlough informs that the bank is in an 
            aggressive marketing mode, and is targeting the oil, trading and 
            manufacturing sectors to selectively grow its corporate loans 
            portfolio. &quot;All our risk-managers are now marketing staff, who 
            clearly understand the market and are in a much better position to 
            tap corporate customers and take a call on the viability of the 
            loan,&quot; he says. <br>
            Things may not be as fine on the private banking front, though. 
            While Carlough insists that the bank does have a &quot;large base,&quot; it is 
            in the process of reviewing its private banking strategy. In fact, 
            even at this moment, OIB is on a lookout for a new head to bring 
            about &quot;radical changes&quot; in the division's performance and to &quot;raise 
            the level of service delivery to a select group,&quot; he says. The 
            radical rethink that Carlough refers to entails redefining the 
            bank's private banking market segments and developing offerings to 
            fit the revised segments. <br>
            <br>
