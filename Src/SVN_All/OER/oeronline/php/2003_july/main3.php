            receiving feedback about its services so as to rectify any problems 
            that may surface from time to time, OIB carries out extensive 
            customer service surveys at regular intervals to &quot;provide 
            competitive service to the consumer,&quot; informs Carlough. &quot;With the 
            service levels already established, an external source will now 
            monitor it, and we will also measure it for intra-departmental 
            service delivery as well,&quot; he adds. <br>
