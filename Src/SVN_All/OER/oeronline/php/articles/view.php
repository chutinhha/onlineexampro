        <p align="left"><font size="4" color="#FF0000"><font face="Verdana">Oil manipulation</font></font></p>
        <p align="left"><font face="MS Sans Serif" size="2">
        <img border="0" src="../../images/article_bp.jpg" align="left" hspace="3" vspace="3" width="250" height="215">It is proving 
        difficult to prevent local shopkeepers from selling bogus engine oil. BP 
        has launched a new initiative to assist in the crackdown of such 
        activities. Jeremy Bowen, Chairman of BP Middle East, speaks to 
        OERonline about the issue of oil manipulation in Oman.</font></p>
        <p align="left"><font face="MS Sans Serif" size="2"><b>Can you describe 
        the problem of oil manipulation in more detail for us?</b><br>
        Jeremy Bowen: It is quite simple. In Oman you bring your car to have an 
        oil change at a puncture shop, the attendant shows you a can of what you 
        believe will be poured into your engine and, hey presto, unsuspecting 
        drivers don't realise that the lubricant inside has long gone and now 
        the contents have absolutely nothing to do with the can's packaging. 
        Unscrupulous puncture shopkeepers often refill the original pack with 
        low grade, or, even worse, recycled engine oil which can do serious 
        damage to your vehicle. </font></p>
        <p align="left"><font face="MS Sans Serif" size="2"><b>How common is the 
        problem? And how does this compare with other countries in the GCC or 
        the world?</b><br>
        Bowen: Manipulation is quite common in South Asia, and, since most of 
        the attendants in puncture shops come from that region they brought the 
        custom of oil manipulation with them. Manipulation does not seem to 
        happen with such frequency in any other GCC country. </font></p>
        <p align="left"><font face="MS Sans Serif" size="2"><b>What linkage with 
        the government have you had to tackle the problem?</b><br>
        Bowen: We are currently in talks with Omani officials in different 
        ministries to find a way to fight manipulation together. BP believes 
        that working with community stakeholders is an integral part of ensuring 
        quality control of products for its customers, as well as educating 
        customers so they know what to look for. </font></p>
        <p align="left"><font face="MS Sans Serif" size="2"><b>And what 
        timetable for its eradication or targets have been set?</b><br>
        Bowen: For BP, as soon as possible. There are no official dates for the 
        eradication of oil manipulation in Oman, but in the interim as a 
        precaution we have launched the new tamper-proof Visco 2000 pack and we 
        are doing our best to educate consumers regarding the many dangers of 
        oil manipulation. </font></p>
        <p align="left"><font face="MS Sans Serif" size="2"><b>What damage does 
        oil manipulation do to the car and environment?</b><br>
        Bowen: Incorrectly formulated oils and recycled oils are the main reason 
        for engine wear and poor car performance. Manipulated oil usually 
        contains a mixture of chemical structures known as PCAs which can cause 
        skin cancer and make the oil unsafe to handle or come out in the car's 
        fumes. Improperly formulated and recycled oils have higher sulphur 
        contents and chemicals that poison catalytic converters which 
        drastically shorten their life.</font></p>
        <p align="left"><font face="MS Sans Serif" size="2">Also, oil volatility 
        is often a problem with these engine oils causing higher hydrocarbon and 
        particular pollutions than properly formulated oils. As well as all 
        this, cheaply formulated engine oils have a much shorter lifespan than 
        properly formulated oils and so need to be drained and changed more 
        frequently. This makes them more expensive to use and causes 
        environmental problems through waste oil disposal problems.</font></p>
        <p align="left"><font face="MS Sans Serif" size="2"><b>Why does BP see 
        it as its mission to crack down on this? Shouldn't this be the work of a 
        consumer watchdog?</b><br>
        Bowen: BP is innovative, and strongly committed to the protection of the 
        natural environment. Our goals are simply stated, no accidents, no harm 
        to people, and no damage to the environment.</font></p>
        <p align="left"><font face="MS Sans Serif" size="2">BP is also committed 
        to ethical business practice in every one of the 100 countries in which 
        we operate, and we are also committed to providing our customers with 
        high quality products. To have low quality substitutes being sold to 
        customers in our name is a major concern given the potential damage to 
        their engines and their perception of our high quality products. <br>
        <br>
        By building a track record of delivering on our promises and of meeting 
        the highest standards we are setting for ourselves, we believe we can 
        demonstrate that our activity in every place in which we operate is to 
        the benefit of the whole community and of every individual.</font></p>
        <p align="left"><font face="MS Sans Serif" size="2"><b>What consumer 
        rights does one have in Oman if this happens to them?</b><br>
        Bowen: First of all, prevention is the best cure. Consumers should ask 
        to check personally that [the oil container they have bought] has</font><font face="MS Sans Serif" size="2">n't 
        been opened before. <br>
        <br>
        However, if BP customers suffer from oil manipulation you should report 
        it immediately to BP Oman and the Omani police, so that further legal 
        action can be taken. This is a serious matter but so far drivers have 
        been complacent and have not reported the puncture shop or the garage 
        that has harmed them. If there is a message that we want to convey 
        today, it is be pro-active, stand up and speak up when you are victim of 
        oil manipulation. This issue affects all of us, and only by fighting it 
        together will we eradicate this hazard to our environment and our 
        vehicles.</font></p>
