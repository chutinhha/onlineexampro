<b>How to Prevent Card Fraud<br>
</b>By Monalisa Nayak<br>
<br>
<img border="0" src="spotlight6.jpg" align="right" width="100" height="185" hspace="5">Though most of us are cautious, still it is not always possible to prevent or 
stop credit card fraud from happening. Cardholders and issuers lose huge chunk 
of money every year due to credit card fraud. There is no escape theory as such 
but there are certain precautions you can take to make it more difficult for a 
crook to capture your card or card numbers and minimize the possibility of any 
deceit.<br>
1. Sign your cards as soon as they arrive<br>
2. One should not carry credit cards in wallet. It should be carried separately 
in a zippered compartment, a business card holder, or any other small pouch.<br>
3. Keep a record of your card numbers, their expiration dates, and the phone 
number and address of each company in a secure place. However, never store the 
written down PIN together with a card. <br>
4. Do not leave a card unsupervised. Keep your card in view whenever any 
transaction takes place. Retrieve your card promptly after using it.<br>
5. Void incorrect receipts. Save receipts to compare with billing statements.<br>
6. Open bills promptly and reconcile accounts monthly, just as you would be your 
checking account. It is pertinent to report any questionable charges promptly 
and in writing to the card issuer.<br>
7. Notify card companies in advance of a change in address.<br>
8. Never give out any personal information over the phone, such as SSN, PIN 
numbers etc. No one not even bank employees have the right to demand it. Only 
enter it in a proper PINpad.<br>
9. Do not allow anyone to see your PIN.<br>
10. Check your credit report regularly. This allows you to see if any there is 
fraud occurrences. <br>
11. Never lend a card to another person not even to your best friend or a close 
relative.<br>
11. Report lost credit cards to your bank immediately. <br>
12. Check statements for unauthorized charges, especially if you have used the 
card while travelling overseas.<br>
13. Never leave cards or receipts lying around. <br>
14. Notify card companies in advance of a change of address.<br>
15. Never give your number over the phone unless you are initiating a 
transaction with a company you know is reputable. If you have questions about a 
company, check with your local Consumer Protection Office or Better Business 
Bureau before ordering.<br>
<br>
<b>Things to keep in mind incase you lose your credit card</b><br>
<br>
<img border="0" src="spotlight8.jpg" align="left" width="150" height="94" hspace="5">If someone loses or misplaces his or her credit card, in such a scenario people 
generally tend to panic or cry over the split milk. Well, that really won’t 
help. Always remember, if your credit or charge cards are lost or stolen, call 
the issuer(s) immediately. Most card companies have a toll-free number for 
reporting missing cards. Some companies provide 24-hour service too these days 
to handle such crisis. This will prevent illegal usage of the card by any 
miscreants.</font></p>

<p align="justify" dir="ltr"><font face="Verdana" size="1"><br>
<b>If suspecting fraud or facing billing problems<br>
</b><br>
Such issues can again give much headache to anyone. However, it is always better 
to know ways to deal such situations. Like if you come to know that someone has 
illegally used your account number, report promptly and in writing. Send the 
card issuer a letter that includes your name, account number(s), and the charges 
that you question with your reasons. You must direct your letter to the “billing 
error address” provided on your statement. Moreover, open billing statements 
promptly and reconcile your card accounts each month, just as you would your 
checking account. Also, save all receipts to compare with your billing 
statements.<br>
