<br>
</font><b><font face="Verdana" style="font-size: 14pt">Pleasures of Plastic 
Money</font></b><font face="Verdana" size="1"><b><br>
By SARADA VISHNUBHATLA</b><br>
<br>
</font><font face="Verdana" size="2"><b>P</b></font><font face="Verdana" size="1">assé are the days when one carried wads of hard currency in one’s pocket. Well, 
almost. Nowadays, be it young working adults or seasoned businessmen, it is with 
a studied finesse that they use chic-looking plastic cards in place of money to 
shop – for both essential and not so necessary items. Plastic money in the form 
of Credit, Debit and Smart cards is relatively a new concept in GCC, especially 
so in the Sultanate of Oman. Latest figures show that the credit card holders in 
Oman are a little less than 100,000 while debit cardholders would be about 
1.5million.World over; virtual money has become an essential part of daily life.<br>
<br>
<b>Playing the cards well</b><br>
<br>
<img border="0" src="spotlight1.jpg" align="left" width="125" height="190" hspace="5">Simply put, a credit card enables a person to buy now and pay later. A debit 
card on the other hand, deducts money from the savings account attached to it 
enabling one to buy goods and pay simultaneously, while a smart card belongs to 
a totally different category. It is futuristic and is yet to hit Oman market 
seriously.<br>
<br>
“By very nature, credit cards allow the consumer to go beyond his spending limit 
at any given moment. On the other hand, a person who is risk-averse - meaning 
who avoids debt of any kind - generally opts for debit cards. “In the last five 
years, one has seen tremendous improvement in the usage of cards in Oman. 
Earlier, customers only used cards to withdraw money. Omanis are more educated 
now about it. They now use credit cards to even buy groceries, “ is what Farah 
Abdulla Moosa, Manager of Oman Arab Bank’ s Visa Card Centre, has to say about 
the gradual rise in the number of credit card users.</font></p>

<p align="justify" dir="ltr"><font face="Verdana" size="1">“This is a dynamic 
industry and anything is possible. Electronic payments are fairly young when 
compared to cash, but is a rapidly growing one. World over, plastic payments are 
gradually displacing cash in many areas such as daily consumer payments and even 
for corporate and government transactions. Consumers are increasingly mature in 
their payment habits and with technology as a key enabler, we will definitely 
see a very significant proportion of cash transactions being replaced by 
electronic payments,”thinks Omar Alwi, Country Manager for Oman, American 
Express Middle East.<br>
<br>
A smart card is technologically advanced than both credit and debit cards. The 
electronic chip installed on it recognises and reveals at request a consumer’s 
identity instantly. They are designed to store medical history, passport 
details, blood group and even fingerprints. This helps both the consumer and the 
service provider. “The entire world is moving towards a chip card. One can see 
an incremental growth in debit cards which is far higher than credit cards,” 
says Mudit Saxena,Head of Marketing, Bank Dhofar.<br>
<br>
“Cards offer greater convenience, security and flexibility than cash. They are a 
great payment tool that complements the modern lifestyle of consumers, providing 
them an easy and safe payment channel,” says Omar.<br>
<br>
<b>Cash is the King</b></font></p>

<p align="justify" dir="ltr"><font face="Verdana" size="1">In Oman unlike other 
countries, cash still rules the day. A general trend in behaviour indicates that 
plastic money comes into usage only when they are travelling abroad. “It cannot 
replace money. People still need to carry hard cash. It gives them a sense of 
security,” says Moosa of OAB. Contrarily, Ravneet Chowdhury of Standard 
Chartered Bank’s Head of Client Relationships believes,” cash is going out of 
fashion. World is opening more. So carrying wads of cash is impractical.”</font></p>

<p align="justify" dir="ltr"><font face="Verdana" size="1"><br>
The uptake on virtual money is still low here in comparison to the rest of the 
world. One reason could be that Oman offers low wages to its employees in 
comparison to other GCC countries like Qatar. Combine it with the fact that 
virtual money is a relatively new concept. As a result, the locals are not very 
comfortable with the idea. Only the expatriates and the Omanis who have had 
education abroad opt for the plastic money, as they would have had tasted the 
convenience offered by them.<br>
<br>
<b>Big players<br>
</b><br>
<img border="0" src="spotlight10.jpg" align="right" width="150" height="105" hspace="5">The presence of international banking giants like American Express, HSBC, Diners 
Club, Global Card Services, Visa Electron and Standard Chartered in Oman has 
triggered proprietorial instincts in the local players. National Bank of Oman, 
Oman Arab Bank, Bank Dhofar, Bank Muscat and Oman International Bank stand their 
ground firmly rooted in the fact that they set up business long before the world 
biggies made an entry in Oman besides commanding the local loyalty. At the same 
time, the local banks are aware of the fact that they have to be EMV compliant 
dictated by international banking standards, sooner rather than later. EMV 
compliance strives to put a stop to fraudulent transactions through skimming and 
other kind of thefts.<br>
<br>
Consumers also get attracted to the fact that Omani banks offer Islamic banking. 
“Bank Dhofar is the first to launch Islamic credit card Al Noor for which we 
only charge a fee,” says Saxena of Bank Dhofar. Islamic credit cards offer more 
than convenience to its users. It is one with their personal religious 
preferences. But OAB’s Moosa feels that competition is on a different platform 
altogether, “it is more with the whole banking service rather than it being 
credit card oriented.” The reason is that most of the local banks require a 
consumer to hold a savings account with them if a credit card has to be issued. 
But with international banks this feature is mutually exclusive.<br>
<br>
Nevertheless, each bank relies upon its USP to expand its consumer base. 
Chowdhury of Standard Chartered says the bank’s unique selling point lies in the 
service quality they offer to the consumers. He says, “Over 80% of our sales are 
done through mobile sales consultants who visit customers at their convenience.” 
Asif Redha, Acting Head of NBO’s Consumer Banking Division says, “On the other 
hand, the USP with a bank like NBO lies in the fact that it has a ‘Money Back’ 
plan where credit card customers are rewarded for use of cards in either the 
merchant outlet or for cash withdrawal. Immediacy is Oman Arab Bank’s USP. Moosa 
supports the claim when he says, “we get calls from abroad (from our traveling 
customers) and we take action on the spot.” Bank Dhofar offers first interest 
free credit card in Oman attracting newer clients.<br>
<br>
“Our cards are a passport to exclusive privileges and our card members choose us 
for the world class service and personal recognition,” feels Omar, of American 
Express.<br>
<br>
Apart from the regular cards, most banks offer credit cards exclusively for 
Internet usage. These cards are easy to use and payment is also free of any 
technical hassles garnering tremendous popularity with the masses.<br>
<br>
<b>Co branding is big<br>
</b><br>
Companies have benefited tremendously with the concept of co-branding. The basic 
formula at work is to well-established brands come together creating a win-win 
financial situation for all the entities involved. For example, when an airlines 
and a credit card come together, they share their customer base. They build 
loyalty programmes triggering higher revenue for both brands while dangling free 
offers to lure and keep customers. A case in point is NBO’s combined card in 
conjunction with Master Card. Saxena of Bank Dhofar points out that, “It works 
but out of the five odd cards a consumer has in his pocket, to get him to use 
your own card your programmes have to be really good. Oman market is ready for 
more and more such programmes.”<br>
<br>
“Customers today seek affinities with the brands and the services they buy. They 
are buying not just the tangible but also the intangible benefits of the brands. 
Co-branding programmes draws out the best of both brands in meeting the card 
members’ lifestyle and emotional needs. We have successful co-branding products 
in other markets in the Middle East such as the Dubai Duty Free Card in the UAE 
and the recently launched BMW Credit Card in the UAE and Bahrain,”said Omar.<br>
<br>
Utility services like communication, airlines, and fuel stations have big 
consumer bases. Current trends show that both banks and other companies are 
either starting to step into the co-branding territory or are at least thinking 
about it. Nonetheless, it is a fact that the percentages and the co-branding 
market are still small with matching margins.<br>
<br>
<b>Risks and Defaults<br>
</b><br>
<img border="0" src="spotlight3.jpg" align="left" width="124" height="171" hspace="5">Issuer banks have to contend with potential defaults on part of the consumers 
ranging from intentional credit runaway, circumstantial default to habitual 
delinquency. On the other hand, two major risks for the banks are the credit 
risk and fraudulent use of cards through skimming. “Being a global cards player, 
we have developed core competency in risk management. We help consumers who are 
facing difficulties in repayment of debts – often it is a question of reminding 
customers about missed payments,” Chowdhury of Standard Chartered offers an 
olive branch to the customers. Placing faith in the consumer’s repayment 
abilities is often a remedy against a potential panic-attack to the banks.<br>
<br>
Says Moosa of Oman Arab Bank, “We are a conservative bank. For us to give a card 
to a customer, we have a standard that a customer has to meet. These standards 
are high. It is difficult for a bad or a huge risk to materialise. Risk is 
minimal.” Bank Dhofar’s Saxena opines,” most people here live beyond their 
credit limit. So you have to build a risk profile. But majority consumers do pay 
back in time.” Despite the risks and defaults, credit cards make a highly 
lucrative business, which no bank would like to lose. Developed nations work 
through a central database of perpetual defaulters. Once blacklisted, no other 
bank entertains the defaulter. Such an exercise is still in its infancy in Oman.<br>
<br>
Says Omar, “Generally, we manage credit losses on a portfolio basis ensuring a 
targeted trade off against returns. We employ technology based statistical 
models for assigning credit exposure and determining appropriate collection 
strategies”.<br>
<br>
<b>The Sweeteners <br>
</b><br>
Free weekly movies, dining offers, cable television discounts and travel perks 
top the list of Standard Chartered freebie offers besides distinct benefits like 
purchase protection and lost card protection. The bank also offers photo sign 
cards for additional safety. Five supplementary cards are also on offer with 
every primary card.<br>
<br>
In celebration of the 10th year of Membership Rewards, American Express has 
introduced a special ‘Double Rewards’ <br>
<br>
campaign, giving away six million points which can be redeemed for a host of 
lifestyle benefits. The comprehensive range of benefits include travel, fine 
dining and leisure activities and fantastic shopping rewards.<br>
<br>
Cashing in on the football fever, NBO ran a special promotion where customers 
were offered NBO FIFA MasterCard credit card for over six months. 15 lucky 
customers were chosen to watch selected world cup matches live. Currently 
running is a special summer offer with monthly prizes to win by every credit 
card holder. OAB on the other hand has weekly draws for its credit card holders. 
Currently on offer is a holiday package to one of the four destinations — 
London, Cairo, Bangkok and Kuala Lumpur — for two people in the months of June 
and July; all expenses paid for including the airfares and taxes, five start 
hotel accommodation for five days.<br>
<br>
Khareef festival promotional offer is what Bank Dhofar has for its cardholders. 
The package comprises of a weekend holiday in Salalah for two people to be flown 
by Oman Air for a total of 3 nights stay at the 5 Star - hotel complete with 
breakfast.<br>
Virtual money may not have the distinct smell of a freshly minted currency note 
but in view of its convenience and safety, it sure wins hands down over the good 
old cash.<br>
