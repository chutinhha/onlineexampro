<b>GCC Travel and Tourism (T&amp;T) Forecast<br>
</b><br>
<img border="1" src="gcc_dairy_2.jpg" align="right" width="150" height="96" hspace="5"><b>The 
UAE:</b> T&amp;T in United Arab Emirates in 2006 is expected to generate US$26,353 
million of economic activity (total demand). UAE’s T&amp;T economy (direct and 
indirect impact) in 2006 is expected to account for 12.1 per cent of GDP and 
294,000 jobs (11.7 per cent of total employment). UAE’s T&amp;T is expected to grow 
6.6 per cent in 2006 and by 5 per cent per annum, in real terms, between 2007 
and 2016.<br>
<br>
<b>Bahrain: </b>T&amp;T in Bahrain in 2006 is expected to generate US$4142.2 million 
of economic activity (total demand). Bahrain’s T&amp;T economy (direct and indirect 
impact) in 2006 is expected to account for 20.9 per cent of GDP and 84,000 jobs 
(24.4 per cent of total employment). Its T&amp;T is expected to grow 12.2 per cent 
in 2006 and by 4.8 per cent per annum, in real terms, between 2007 and 2016.<br>
<br>
<b>Oman:</b> T&amp;T in Oman in 2006 is expected to generate US$4566.2 million of 
economic activity (total demand). Oman’s T&amp;T economy (direct and indirect 
impact) in 2006 is expected to account for 9.7 per cent of GDP and 83,000 jobs 
(10.6 per cent of total employment). Oman’s T&amp;T is expected to grow 4.4 per cent 
in 2006 and by 5.3 per cent per annum, in real terms, between 2007 and 2016. <br>
<br>
<b>Qatar:</b> T&amp;T in Qatar in 2006 is expected to generate US$5596.4 million of 
economic activity (total demand). Qatar’s T&amp;T economy (direct and indirect 
impact) in 2006 is expected to account for 11.4 per cent of GDP and 39,000 jobs 
(11.6 per cent of total employment). Its T&amp;T is expected to grow 10.4 per cent 
in 2006 and by 4.1 per cent per annum, in real terms, between 2007 and 2016. <br>
<br>
<b>Saudi Arabia:</b> T&amp;T in Saudi Arabia in 2006 is expected to generate 
US$34822.5 million of economic activity (total demand). Saudi Arabia’s T&amp;T 
economy (direct and indirect impact) in 2006 is expected to account for 8.2 per 
cent of GDP and 284,000 jobs (8.7 per cent of total employment). The country’s 
T&amp;T is expected to grow 1.4 per cent in 2006 and by 5.1 per cent per annum, in 
real terms, between 2007 and 2016.<br>
<br>
<b>Kuwait:</b> T&amp;T in Kuwait in 2006 is expected to generate US$10,524.6 million 
of economic activity (total demand). Kuwait’s T&amp;T economy (direct and indirect 
impact) in 2006 is expected to account for 7.3 per cent of GDP and 87,000 jobs 
(7.3 per cent of total employment). Kuwait’s T&amp;T is expected to grow 3.7 per 
cent in 2006 and by 3.8 per cent per annum, in real terms, between 2007 and 
2016.</font></p>

