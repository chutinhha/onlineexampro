In their efforts to further integrate their economies, Qatar and Bahrain are 
virtually racing against time. In June, authorities of both countries took some 
extraordinary steps designed to strengthen economic ties. The two countries 
signed wide-ranging contracts, including — notably — construction of a 
long-awaited causeway. The 40-km structure aims to link north-western Qatar to 
south-eastern Bahrain. <br>
<br>
The project is estimated to cost some US$3 billion, borne equally by the two 
governments. The two countries can absorb the funding on the back of firm oil 
prices in international markets. The petroleum sector accounts for three 
quarters of treasury income in both Qatar and Bahrain.<br>
<br>
Tenders related to construction parts of the causeway are due to be issued 
during the summer, with contracts to be granted by year-end. If everything goes 
according to the plan, the causeway should become operational by 2011. <br>
<br>
The link should bring economic benefits to both nations and particularly to the 
smaller Bahrain. Benefits include invigorating local economies, strengthening 
trade relations and enhancing integration among GCC countries. All such efforts 
can lead to stronger levels of growth of the gross domestic product (GDP) of 
Qatar and Bahrain.<br>
<br>
<b>Stimulating Economic Activities <br>
</b><br>
To begin with, local economies of both countries stand to benefit from spending 
on the project. Each country is scheduled to spend some US$1.5 billion over a 
span of four years, the period needed to accomplish the scheme. Undoubtedly, the 
GDPs would benefit from this sizeable amount, with funds finding way to 
different contractors. Then, there is the multiplier effect. Traditionally, each 
dollar spent generates more than three dollars in the economies of both Qatar 
and Bahrain.<br>
<br>
<b>Improving Trade Relations <br>
</b><br>
The link would certainly assist in increasing the level of bilateral trade. At 
the moment, Bahrain enjoys trade surplus with Qatar. In 2005, the value of 
Bahraini exports amounted to more than US$106 million. This is a sizeable 
amount, as it comprised nearly 5 per cent of Bahrain’s non-oil exports. Yet, the 
value of imports from Qatar amounted to merely US$20 million last year. 
Eventually, the causeway would open up prospects for further trade by making it 
easy for enterprises to engage in trading activities. Already, there are talks 
of Qatar’s industrial projects such as steel finding easier ways to reach the 
Bahraini market. For Bahrain, the causeway provides easy access to the region’s 
fastest growing economy. Qatar’s gross economy grew by more than 30 per cent in 
nominal terms in 2005 (but less in real terms, reflecting inflationary 
pressures). <br>
<br>
<b>Creating Job Opportunities <br>
</b><br>
Improved trade relation helps in keeping some jobs while creating new ones. To 
be sure, the link can provide an avenue for Bahraini nationals looking for 
employment opportunities in Qatar. Bahraini workers possess certain skills, 
including the ability to communicate in English. Unemployment is a serious 
problem in Bahrain (some 14,000 locals have registered themselves as jobless 
under the newly launched National Employment Programme). The figure represents 
nearly 11 per cent of the local workforce. For its part, the Qatari economy 
needs qualified workers, who in turn share local culture. Of all GCC countries, 
the Qatari and Bahraini societies are linked by strong family ties, thanks to 
inter-marriages. <br>
<b><br>
Integrating GCC Economies <br>
</b><br>
Last but not least, the link falls in line with the broader goal of achieving 
economic integration among regional states, the GCC, of which both Qatar and 
Bahrain are members. The GCC aims to become a common market by the end of 2007. 
In turn, this necessitates granting unrestricted access to factors of production 
of member states. Thus, the causeway fulfils a strategic goal of GCC states. <br>
<br>
Qatar’s size stands at 11,437 sq km, which is 16 times larger than that of 
Bahrain. Also, Qatar’s GDP is more than twice the size of that of Bahrain. Yet, 
the causeway would link and bind the two countries and economies once and 
forever. </font></p>

