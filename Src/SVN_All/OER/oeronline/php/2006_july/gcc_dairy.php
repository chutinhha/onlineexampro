Al Rayan Bank was listed on the Doha Securities Market (DSM) on June 19, with 
its shares opening at QR18 (RO1=approx. QR9.40). Many of the GCC investors 
described the listing price as low. The Rayyan IPO earlier this year was one of 
the biggest the region had seen, and its listing on the DSM was viewed as a road 
to overnight riches, by investors across the Gulf.<br>
<br>
Most of the investors were expecting the opening price to be at least between 
QR50 and QR60. Many of the GCC investors felt this was not worth the effort they 
had taken in coming to Qatar. However, some of the investors justified this 
pricing, saying it would benefit long-term and serious investors.<br>
<br>
At a press conference after the listing of the share, the Chairman of Al Rayan 
Bank’s board, Dr Hussain Al Abdullah, justified the price saying it reflected 
the bank’s long-term prospects. “Long-term investors would prefer this price 
level,” he said, arguing that had the price been higher, it would have led to 
speculative trading. <br>
<br>
Some of the investors agreed with this view as they felt that at this price 
level there would not be much selling pressure, thus lending stability to the 
shares. They were of the view that this price was good to buy but not to sell.<br>
<br>
According to informed sources, it were mostly the GCC investors who had placed 
sell orders and they had specified ‘market price’ as the offer price for sale 
and this led to the opening price being low. <br>
<br>
There were many who quoted offer prices at between QR10 and QR15. However, not 
all GCC investors, who had descended in Doha in large numbers for the historic 
event, were in favour of selling the shares. <br>
<br>
<b>International Firm Hired</b><br>
A US-based international consultancy has been hired to help set up the 
multi-billion dollar Al Rayan Bank, which is tipped to be the largest Islamic 
bank in the Middle East. <br>
<br>
Mercer Oliver Wyman is conducting studies in two phases to facilitate the QR7.5 
billion bank’s launch. At the end of phase one, the bank will become 
operational, offering different services and products and with an extensive 
strategy and identity of the bank. The second phase of the study would focus on 
developing these products and services. “We are going to offer the services and 
products no other bank in the region is so far offering,” said Al Abdullah. The 
bank will open branches in the rest of the GCC. <br>
<br>
Most of the banks in the region are focusing on the West and Al Rayan will be 
the first to pay attention to venture capital and real estate, mainly in Asian 
countries like Japan, Korea and Malaysia and the emerging markets like China and 
India, he said.<br>
<br>
Al Rayan Bank also hopes to soon launch an investment bank via the Qatar 
Financial Centre (QFC) by September or October this year, according to the 
chairman of the bank’s board, Dr Hussain Al Abdullah. The bank will focus on 
venture capital, asset management and real estate in the emerging markets in 
Asia and other Asian countries.<br>
<br>
— Vani Saraswathi from Qatar<br>
