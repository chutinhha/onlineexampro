The Kuwaiti government is studying a proposal to introduce income tax for the 
first time in the country. The new law would impose a flat rate tax of 10 per 
cent on all earners, irrespective of their nationality. The draft law is set to 
be studied by the Cabinet and then possibly referred to the next session of 
Parliament.<br>
<br>
A comprehensive draft law prepared with inputs from a Dutch financial 
institution was already sent to the Cabinet for review. The law aims at 
restructring public finances and supporting economic development.<br>
<br>
“Financial and economic reform cannot be achieved without developing the tax 
system similarly to what is being applied in both industrial and developing 
countries,” Finance minister Bader Al Humaidhi told the Cabinet recently.<br>
<br>
Currently, individuals — either Kuwaitis or foreigners — are not subject to 
taxes on income. Besides, Kuwaiti firms enjoy no tax regime. However, foreign 
companies engaged in commercial activities in Kuwait are subjected to income 
tax. Recently a bill was approved by the Cabinet that slashed tax on foreign 
firms to a flat 15 per cent from current levels of up to 55 per cent. The new 
income tax law is expected to be referred to Parliament after the election of a 
new Assembly on June 29.<br>
<br>
<img border="0" src="gccdairy_uae.jpg" width="200" height="40"><br>
Trade between UAE and the European Union (EU) grew by 29 per cent for the first 
quarter of this year to US$10.2 billion compared to nearly US$7.958 billion for 
the corresponding period last year. According to the latest EU statistics, UAE’s 
exports to the EU grew by 10.9 per cent. The total value of these exports rose 
from US$2.02 billion to US$2.24 billion. The total value of imports from the EU 
grew by 34.5 per cent, from US$5.93 billion for the first quarter of 2005 to 
US$7.99 billion for the first quarter of this year.<br>
<br>
The total trade between the UAE and the EU countries grew by 54 per cent to 
US$44.75 billion last year compared to nearly US$30.62 billion in 2004. The 
trade deficit of UAE with the EU rose by nearly 15 per cent in 2005. The total 
volume of deficit hit US$19.62 billion in 2005, up from US$17.06 billion in 
2004.<br>
<br>
The UAE ranked eighth among the biggest exporters to the EU last year and 16th 
among the most significant commercial partners of the EU for the same year. The 
UAE’s share in the EU’s total exports hit 2.38 per cent in 2005, rendering UAE 
in the 25th place among the biggest importers from the EU.<br>
<br>
Statistics show that UAE’s exports to the EU grew by 85.4 per cent from US$6.78 
billion in 2004 to US$ 12.567 billion while imports grew by 35 per cent to US$ 
32.83 billion in 2005 compared to nearly US$23.84 billion in 2004.<br>
<br>
According to the EU, the overall trade with UAE grows at an annual average of no 
less than 20 per cent while the average growth of UAE’s imports hits 15.8 per 
cent and exports 36.4 per cent. Transport equipment and machinery takes the 
first place as the UAE’s most important exports to the EU, claiming a stake of 
57.2 per cent of the total UAE’s exports to the EU last year. The value of these 
exports touched nearly US$7.20 billion.<br>
<br>
These are followed by fuel products and oils, which were worth US$2 billion last 
year and formed nearly 15.9 per cent of UAE’s exports to the EU countries. The 
value of the exports of manufactured products touched nearly US$1.91 billion. 
Transport equipment and machinery also had the lion’s share of UAE’s total 
imports from the EU. The share of these imports touched nearly 62.5 per cent and 
their value reached US$20.14 billion.<br>
<br>
According to the trade statistics, UK made major contribution to the growth of 
UAE’s exports, posting an increase in exports of 87 per cent. The other country 
that made significant contribution is Finland with 46 per cent. On the other 
hand, exports of France declined by 25 per cent while those of the other 
countries were below the average growth at 21 per cent for Netherlands, 19 per 
cent for Germany and 14 per cent each for Belgium and Italy.<br>
<br>
<img border="0" src="gccdairy_saudi.jpg" width="200" height="34"><br>
Jubail Industrial City is getting ready to soon host a number of petrochemical 
and infrastructure projects worth more than US$21.6 billion. The second 
industrial city, Jubail-II, is ready for inauguration.<br>
<br>
The Jubail-II project, covering an area of 1,950 hectares, is expected to 
attract investment projects worth US$58.66 billion. While describing the Jubail 
Industrial City as the largest engineering project in the world, Prince Saud ibn 
Abdullah ibn Thunayan said Jubail-II would provide an expansion facility for the 
industrialization of the city for the next 25 to 30 years.<br>
Jubail project was opened in December 2004. Many new petrochemical, oil and 
infrastructure projects are coming up in Jubail-II, which is expected to create 
some 55,000 job opportunities. Other projects that are in the pipeline include 
the Butanediol plant of the Saudi International Petrochemical Company (SIPCHEM). 
The plant, the first of its kind in the Middle East, has an annual capacity of 
75,000 tons. SIPCHEM recently signed a US$1.06 billion contract with a 
consortium of Canadian, German and French firms for the construction of acetic 
acid and vinyl acetate plants in Jubail. It will have an annual production 
capacity of 460,000 tons.<br>
<br>
The King will also launch the US$21.33 million expressway, linking the present 
industrial city with Jubail-II. <br>
<br>
Other projects to be initiated by the King include a US$97.06 million port and 
wharf for petrochemicals at King Fahd Industrial Port covering an area of 
800,000 sq mt, development of the Jalmouda neighborhood at a cost of US$65.06 
million and a housing project to build 621 housing units for the employees of 
the Royal Commission at a cost of US$115.46 million.<br>
<br>
King Abdullah will also kick off the expansion of the Royal Commission Hospital 
costing US$18.66 million, the National Industrial Gas Company project, which 
requires investments worth US$253.33 million and the Eastern Petrochemical 
Company project (Sharq) at a cost of US$3.48 billion.<br>
<br>
Other projects expected to take off include the United Petrochemical Company, 
with an investment of US$373 million, the Saudi Iron &amp; Steel Company (Hadeed) 
with an investment of US$300 million and the Saudi Arabian Fertilizer Company (SAFCO) 
involving an investment of US$541 million.<br>
<br>
— Sowmya Sri Rallabhandi<br>
