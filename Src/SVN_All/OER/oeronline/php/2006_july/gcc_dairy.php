<p align="justify" dir="ltr" style="margin-left: 5px; margin-right:5px"><font face="Verdana" size="4" color="#FF0000">
<br>
</font><b><font face="Verdana" size="4">Tapping the tourism 
potential</font></b><font face="Verdana" size="1"><br>
<br>
<b>Gulf states are banking on the tourism industry with a view to diversifying 
their economies</b><br>
<br>
<img border="1" src="gcc_dairy_1.jpg" align="left" width="150" height="102" hspace="5"></font><font face="Verdana" size="2"><b>T</b></font><font face="Verdana" size="1">o 
reduce the dependency on oil revenues, Gulf countries are concentrating on 
developing other sectors, especially the tourism industry. More than inbound, 
it’s the outbound tourism market that is flourishing in the region.<br>
<br>
According to the World Tourism Organization, Arab tourists from the Gulf States 
alone spend over US$12 billion on overseas vacations — a very lucrative market. 
Saudi Arabia is one of the biggest outbound markets with tourists from the 
kingdom spending US$6.7 billion annually on overseas travel, which is about 5 
per cent of the country’s GDP. And UAE travelers alone spend over US$4.9 
billion, an average of US$1,700 per trip, which is US$500 higher than the 
European average.<br>
<br>
Looking at the growing demand in the region, as many as six national tourism 
boards from Europe, Asia and Africa have started their Middle East operations 
from Dubai. Followed by Germany, Turkey, Cyprus, Singapore, Malaysia and Morocco 
have all set up shop in Dubai.<br>
<br>
But slowly Gulf countries also are taking pains to attract international 
tourists other than its region. To start with, the Kuwaiti government framed a 
National Tourism strategy to pep up tourism contribution to its GDP. Currently, 
tourism only contributes 1.9 per cent of Kuwait’s GDP. But efforts are being 
made to increase tourism contribution to GDP to 2.9 per cent by 2025, cutting by 
10 per cent, the amount of money Kuwaitis spend annually on tourism outside the 
country. By 2010, the strategy envisages an increase in the number of tourists 
visiting Kuwait to one million per year. This would represent an overall 
increase in tourism numbers of 25 per cent by 2010. <br>
<br>
Saudi Arabia is concentrating on religious tourism. The Supreme Commission for 
Tourism is trying to remove obstacles for foreign tourists to visit the country. 
It has started offering pilgrim visas for over seven months of the year. Not 
just Jeddah but other places like Mecca, Medina, Yanbu and Al Khobar are also 
being given equal priority for tourism development.<br>
<br>
Bahrain, once known as the Pearl of the Gulf, is trying to reinvent itself. 
Tourism industry currently contributes 9 to 10 per cent of the Gross National 
Product. The government is trying to increase it to 20 per cent in coming years. 
The Tourism Affairs department is working hard to improve the inbound tourism.
<br>
<br>
A majority of the Bahrain tourists — about 80 per cent — are from the 
neighbouring Gulf countries. In 2004, five million tourists visited the country. 
The government aims to increase this number to seven million by 2010. The 
efforts of Crown Prince Sheikh Salman bin Hamad Al Khalifa in this regard have 
proved fruitful till now. He has ably used Grand Prix Formula One race to 
promote and boost the tourism sector in the Kingdom. In 2005, the Grand Prix had 
more than 77,257 visitors, 54 per cent of them from Bahrain, 25 per cent from 
the GCC region and a further 20 per cent being international visitors, many from 
the wider Middle East. <br>
<br>
Bahrain’s location gives it an advantage for tourism inflow from Saudi Arabia. 
But it has to improve inbound tourists from outside the region. Formula One race 
is one such opportunity for the country to attract international tourists.<br>
<br>
The Qatar government, on the other hand, framed a Tourism National Plan to 
double the number of visitors from 500,000 in 2004 to one million by 2010. Qatar 
is a less preferred tourist destination in the region. However, Qatar Tourism 
Authority projects that the number of tourists will double from the current rate 
of 700,000 per year to 1.4 million by 2010. To achieve the target, Qatar has 
envisaged its tourism plan around sport events. Grand Prix — motorcycling race, 
Qatar Golf tournment, Tennis open and Squash tournment and many other events are 
expected to attract tourists to this country. The 15th Asian Games, to be held 
in December 2006, are expected to draw a large number of visitors.<br>
<br>
Coming to Oman, 85 per cent of its tourists come from the UAE. To attract 
tourists from places other than the UAE, the Sultanate is taking help of 
professionals. It appointed Dubai based Fusion Marketing to promote and create a 
brand image for Oman. Popular for its culture, Oman is basing its tourism 
strategy on festivals. Salalah Khareef Festival is one such major event promoted 
to attract visitors during summer. But again, this festival attracted 88 per 
cent of its tourists from the GCC countries only. The Ministry of Tourism of the 
Sultanate has chalked out very ambitious targets to achieve a growth from 
current 0.9 per cent of GDP to 3 per cent in the tourism industry by 2020.<br>
<br>
Finally, the UAE undoubtedly leads the tourism industry in the Gulf region. With 
a variety of events — sports, leisure and entertainment, the UAE is able to 
attract a large number of visitors than other GCC states. Surprisingly, this 
number is not only sustaining but also increasing year after year.<br>
<br>
Dubai’s mega projects such as Dubailand, the Waterfront, Palm Islands and 
Festival City will help in increasing the number of visitors by 7.2 per cent 
annually till 2015, taking the number to 14 million, according to World Travel 
and Tourism Council. If Dubai turned into a ‘Mecca of Shopping’, the other 
Emirates of UAE, Abu Dhabi, Al Ain, Sharjah, Fujairah and Ras Al Khaimah have 
become major tourist attractions for heritage, architecture and landscapes.<br>
<br>
— Sowmya Sri Rallabhandi</font></p>
