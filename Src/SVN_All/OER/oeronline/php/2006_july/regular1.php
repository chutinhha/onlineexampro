<p align="justify" style="margin-right: 10px"><b>
<font face="Verdana" color="#FF0000" style="font-size: 14pt">
<br>
</font></b><font face="Verdana" style="font-size: 14pt"><b>‘Work never finishes 
in mind’</b></font><font face="Verdana" size="1"><b><br>
Anurag Guglani, the new General Manager of Electronics &amp; Home Appliances, OTE 
Group, narrates how he is leading his team to achieve goals that earlier seemed 
impossible </b><br>
<br>
</font><font face="Verdana" style="font-size: 11pt"><b>
<img border="0" src="atthehelm.jpg" width="120" height="178" align="left" hspace="5">I</b></font><font face="Verdana" size="1"> 
always wanted to be a fighter pilot but despite coming very close to realizing 
my dream I had to leave it in between due to personal reasons. But nothing could 
stop me from flying on the ground as I moved into management profession. After 
finishing my National Defence Academy (India) training after the school, I did 
MBA from Symbiosis Institute of Management Studies, Pune (India). I worked for 
companies including Indian Express Group, Gestetner (office automation) and 
Softel Appliances (innovative kitchen appliances) etc. Thereafter I moved to 
Dubai where I honed my skills in retail operations. After Dubai, I moved to 
Qatar where I was handling major brands like Philips, Whirlpool, Siemens, Daewoo 
etc., across different product categories. I was there for seven years before 
moving to Oman three months back.<br>
<b><br>
Challenge in job </b><br>
I always believe that one’s work should be challenging enough to keep the 
excitement and motivation going. I personally take up roles which are problem 
solving in nature and require a completely different strategy and mental 
outlook. That’s my strength which has kept me going all these years. Thanks to 
my profession and penchant for exploring new places, I have traveled all over 
the world extensively. I have been closely associated with the top management of 
number of global brands. I’m on Electrolux’s Global Distributor Council which 
consists of just 10 persons all over the world. This council which represents 
the distributors meets every six months to formulate the marketing strategy for 
Electrolux. In June, we are meeting again in Dublin. <br>
<b><br>
Entry in Oman</b><br>
I joined OTE primarily to work on LG as it is a very aggressive brand. It has 
come up strongly in most of the markets. I’m here to propel the company to the 
next level by looking at a 50 per cent growth rate this year. It is very high 
compared to the overall market growth rate which is hovering around 12-13 per 
cent. We are already on target in achieving our goal so far. Besides LG, we are 
also expanding our brand and product portfolio by partnering with companies. In 
the last seven years I had been fighting against the Korean brands. But the role 
has revered now! I’m also bringing new innovative products in Oman market. So 
the thinking is beyond moving boxes and focuses more on strategic thinking with 
customer delight as the fulcrum. <br>
<b><br>
Learnings</b><br>
In Oman, the market is based on price points instead of feature based selling. 
This can change only when big brands start educating the customers about their 
unique features. LG is already doing this. <br>
In just three months, I get the feel that I have put in one year here as I’m 
working at a very fast pace to learn and produce results. I’m traveling all over 
the Sultanate. I’m visiting the dealers and hypermarkets, and meeting front line 
sales persons and their managers as they give lot of on ground information. As I 
have rich Gulf experience, I’m settling down quite fast. The stress right now is 
on team building and increasing motivation that we can grow at a pace faster 
than our own imagination. I have to inculcate this feeling that we do have 
difficult targets but they are not impossible. People don’t see me as a newcomer 
to the market as I have worked in UAE and Qatar for a good number of years. I 
have a young team which is performing exceedingly well and quick in learning to 
further improvise their feats. To develop our human resources, we organise 
well-designed skills development programmes on a regular basis and to further 
motivate them, we have devised our remuneration package to suit the aspirations 
of our staff.<br>
<b><br>
Interests beyond work </b><br>
I always believe that the work never finishes. It finishes on the worktable but 
it is never finished in the mind. One is constantly thinking of improving and 
growing for self-development. In whatever little spare time I get, I do weight 
training at home. I also like to have lot of interaction with management 
professionals and study personality traits including self-evaluation. It helps 
in developing ourselves. The concept of a life coach is very good especially for 
senior level professionals as I can point out to my team members how they should 
approach their work but who is going to tell me? The life coach can do that for 
me. — As told to Akshay Bhatnagar</font></p>

