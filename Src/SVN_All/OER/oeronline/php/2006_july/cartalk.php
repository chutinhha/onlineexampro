<p align="justify" dir="ltr" style="margin-left: 10px; margin-right: 10px"><font face="Verdana" size="1">
<br>
<br>
</font>
<b><font face="Verdana" size="4">
Pulse-raising sedan</font><font face="Verdana" size="5"><br>
</font>
</b><font face="Verdana" size="1"><br>
<b>Chrysler 300C SRT8, with 0-100km in five seconds straight, is a great value 
muscle car as OER finds out in a test drive</b><br>
<br>
<img border="1" src="cartalk_1.jpg" align="left" width="150" height="95"></font><font face="Verdana" size="2"><b>W</b></font><font face="Verdana" size="1">hat 
makes Chrysler 300C SRT-8 so exceptional is that it is probably one of the few 
sedans that combines the refinement and performance of the expensive super 
sedans with an affordable price tag of RO20,000. The car is meant for those who 
wish to have sports car performance from their sedan without compromising on 
seating and luggage space. The latest addition to Chrysler’s Street and Racing 
Technology Series (SRT) is an excellent combination of power, style, comfort and 
space.<br>
<br>
<b>Power Packed Performance</b><br>
The moment you are in the driver’s seat, there is a special feeling of driving 
an awesome sized head turner a car with the raw power of a 6-litre plus engine. 
It develops 425 horsepower and 420 pound-feet of torque from a 
high-compression-ratio 6.1-litre Hemi V-8. The torque peaks at 4800 rpm. That 
may sound high for an engine of this size but the copious displacement means 
enough torque is available off idle to put the limited-slip differential to good 
use. <br>
<br>
The five-speed automatic transmission gives instant upshifts and downshifts. 
Full-throttle shifts lead to an explosive sonic boom from the exhaust. Back off 
the throttle, and the sound becomes smooth and unobtrusive. Even at over 120 
kmph, you don’t hear the engine much, just the low decibel sound of the blowing 
wind and the humming of the wide tyres when your stereo system is not on. It is 
not light weighing in at 2,280 kg but sprints from 0-100 km in 5 seconds with a 
top speed of up to 280 kmph. <br>
<br>
The weight of the SRT8 is effectively hidden by suspension changes that lower 
and stiffen the chassis. Handling has been upgraded compared to the standard 
300C sedan with a lower ride height (by one-half inch), beefier anti-roll bars, 
stiffer springs, sophisticated Bilstein shock absorbers and Brembo disc brakes. 
Bilstein and Brembo are both hallowed suppliers of racing and high-performance 
parts, which is why they’re tapped for Chrysler’s SRT. The striking 20-inch 
wheels look nearly big enough to double as turbofan blades on an aircraft. The 
tyre sizes are staggered — smaller 245/45R-20 fronts and slightly larger 
255/45R-20 rears. <br>
<br>
The car’s standard Electronic Stability Programme (ESP) is tuned to maximise 
cornering abilities and can allow the car to slide through curves with ease. <br>
<br>
<b>Comfortable Ride<br>
</b><br>
<img border="1" src="cartalk_2.jpg" align="right" width="200" height="84">With 
106.6 cubic feet of passenger room, it’s more than just spacious. The seats are 
supportive and wide enough to fit in the bigger drivers and passengers quite 
comfortably. The standard 60/40-split folding rear seat accommodates long 
objects and passengers alike. The 120-inch wheelbase provides a roomy interior 
and good foot clearance for stepping in and out of the rear seat.<br>
<br>
The well-appointed interior features satin silver accents and an elegant analog 
clock. It offers memory-controlled amenities like power-adjustable pedals, 
driver’s seat and radio presets. So in case the car is going to be used by more 
than one person regularly, the drivers won’t be required to change the settings 
every time as per their comfort. <br>
<br>
SRT-8 is quite popular among the younger segment of the auto buyers. No wonder 
Chrysler has made sure that the stereo system is as good as the rest of the 
features of the car. As a standard fitment, SRT-8 comes with AM/FM stereo with 
an integral six-disc CD/MP3 player and full-screen navigation system. The sound 
generated from Boston Acoustics six premium speakers with subwoofer and 276-watt 
digital amplifier is deafening<br>
<br>
The long hood and tall sedan styling give this vehicle an unmistakably strong 
presence, while available chromed details like body side-molding accents, 
exterior mirrors, and door handles add a touch of style. As I was driving 
through Muscat heads were turning with envy. <br>
<br>
It comes fitted with a rain-sensing wiper system. The infrared rain sensor, 
located on the inside of the windshield just below the rearview mirror, 
automatically activates the wipers when it senses moisture on the windshield and 
adjusts the wiper speed based on the speed of the vehicle, be it rain or road 
splash from the vehicle ahead.<br>
<br>
With mercury soaring above 44 degree centigrade, this may not be the right time 
to bring the outside in with the vent, tilt, and slide power sunroof using the 
one-touch express-open/close feature. Press the button once and the sunroof 
opens to let in the sun or stars or closes to keep out the rain.<br>
<br>
For those who often end up dinging other cars while parking, they will be 
pleased to find the ParkSense rear park assist system quite useful. It uses 
sound waves to detect nearby obstacles in and near the rear path of the vehicle 
to help you maneuver into tight spaces. The audio-visual indicators in the 
overhead display in the rear of the vehicle alert you to the proximity of most 
larger, stationary objects that might be outside your line of sight.<br>
<br>
Like in most powerful vehicles, Chrysler has taken extra care to ensure the 
safety of the passengers in the event of a mishap. Safety cage architecture 
protects occupants by managing and controlling energy in the event of an 
accident. Advanced front multistage air bags are empowered by the occupant 
classification system (OCS) according to the weight of the front passenger. <br>
<br>
Though it would be unfair to make a comparison of SRT-8 with other vehicles, its 
closest competitors are the BMW M5, Cadillac STS-V, Chevrolet Impala SS, Dodge 
Charger SRT8 and Mercedes-Benz E55 AMG. The rich features of SRT-8 — at a very 
competitive price of RO20,000 — makes it one of the most affordable and 
desirable vehicle in its segment in Oman.<br>
<br>
<i><b>Road test courtesy Zubair Automotive LLC, the dealers of Chrysler vehicles 
in the Sultanate of Oman</b></i></font></p>
