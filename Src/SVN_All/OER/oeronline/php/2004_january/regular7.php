            successfully as India is doing? </b><br>
            The major cost of a call centre is the cost of the agent. Obviously 
            a bigger pool of agents is available in India and at very 
            competitive rates. Besides, English is widely spoken in India and it 
            has had a head start over Oman. At the same time call centres offer 
            a large potential for employment of Omani youth and therefore the 
            focus should be on how to make Oman an attractive destination for 
            this business. In India, the number of call centres has grown 
            exponentially as a result the earnings per seat have also dropped. 
            Today, Indian call centres are looking at offering technical help 
            desks that add value to the simple call centre and are also looking 
            at Sri Lanka as a disaster recovery site. Oman could position itself 
            to take some of this business. <br>
