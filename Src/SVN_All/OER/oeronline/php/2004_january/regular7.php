            <b>How difficult is it to Omanise an infotech sector that could 
            benefit from more foreign expertise? </b><br>
            It is difficult because the local talent available is limited. 
            Unfortunately not every Omani graduate is as good as one would 
            expect on the basis of the curriculum. Those who are good are 
            considering employment with the oil companies, banks or the 
            government. The evaluation system adopted by education institutions 
            needs to be reviewed if the quality of pass students is to match up 
            to international standards. At present the performance of teachers 
            and institutions are evaluated on the basis of a pass percentage 
            which, in my opinion, does not necessarily represent the actual 
            quality or level of education that the student has acquired. This 
            is, I think, one of the reasons for the skills gap we face. 
            Omanisation is certainly a socio-economic need and employment must 
            be found for the growing number of Omani youth leaving schools and 
            colleges. At the same time, it is very important that the student 
            assessment system be improved so that quality is not compromised and 
            employers have no hesitation in employing these students. <br>
            <br>
