            <b>What difficulties did you face in steering JST towards such 
            successful growth? </b><br>
            In 1982 JST was a relatively new entrant in the market and the major 
            multinationals in the telecom field already had representations in 
            Oman. To compete, we focused on offering customer premise equipment 
            and on very high quality of after sales service. Most customers want 
            the best product with the best features at the lowest price. The 
            challenge is to convince the customer that the quality products and 
            after sales service that JST offers is far more cost effective than 
            low priced alternatives. This approach has helped us to succeed and 
            has also encouraged major players like Lucent Technologies, Lexmark, 
            and Avaya among others to consider us as their partner in Oman. <br>
            Another major challenge is that our diversification into new 
            business units such as office automation and security/ alarm systems 
            business has required the induction of additional resources and 
            product specialists. However, the market in Oman is relatively small 
            and such costs are therefore spread over thinner sales volumes. 
            Moreover, the challenge we have faced in our working environment has 
            been to retain staff and keep them motivated. Customers tend to 
            equate this constancy with reliability of their solution provider. 
            Happily, we have been successful in this aspect with low staff 
            turnover. <br>
