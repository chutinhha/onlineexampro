            corporates have taken or are in the process of taking the necessary 
            steps but not all the SMEs can afford to or are willing to invest in 
            e-security. While the IT managers within organisations understand 
            this threat they are not always able to obtain the necessary funding 
            from their bosses. There is evidence that these attitudes are 
            beginning to change. <br>
            <b>Access Control:</b> Omani businesses and homeowners are becoming 
            more aware of the need for security and burglar alarm systems. They 
            are investing in various systems from simple automatic door opening 
            devices and intruder alarms to sophisticated CCTV systems with video 
            recording and remote monitoring systems. Our company designs secure 
            solutions for banks, jewellery shops and villas. The level of 
            sophistication is determined by our client's threat perception and, 
            of course, their budget.Biometric scanners and access control 
            systems are the next generation of door entry systems. Our clients 
            for such systems include the Ministry of Defence. <br>
