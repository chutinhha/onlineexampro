                    <img border="0" src="cover14.jpg" align="left" hspace="5" width="200" height="228">The 
                    face of retail in Oman has changed dramatically over the 
                    past few years. With the development of new shopping centres 
                    and hypermarkets, locals who were once served by souks, 
                    street-front shops and medium-sized malls now have a greater 
                    selection of brands to choose from. Although this has 
                    increased competition in the local market, it is keeping a 
                    greater percentage of retail spending in the country and 
                    gives local Omanis viable career paths in the retail sector.
                    <br>
                    <br>
                    There are an increasing number of Dubai-based international 
                    brands looking to expand into the Muscat market. Today, the 
                    retailer that fails to offer exceptional customer service, 
                    selection of merchandise and quality products will find it 
                    difficult to survive in the increasingly competitive Muscat 
                    market. <br>
                    <br>
                    In the last twenty years, consumer demographics and 
                    attitudes have undergone a considerable change Ð the Omani 
                    population has become younger, more urbanised, more 
                    demanding in terms of the shopping environment because of 
                    the exposure to international trends, more willing and 
                    capable of trying out new products because of the increased 
                    buying power, and more exposed to suppliers' marketing 
                    efforts Ð all of which has helped the development of the 
                    supermarket concept in Oman. <br>
                    <br>
                    There is a lot of similarity between Oman's food retail 
                    segment and the ones in other GCC countries. An extensive 
                    selection of food products and complementary services is 
                    available through the supermarkets and self-service stores 
                    in the country. The food retail in the country is becoming 
                    more diversified and is divided into the following 
                    categories: supermarkets; convenience stores/ gas marts; 
                    institutional retailers (non-profit retailers serving the 
                    military and police force); cash and carry, wholesalers; and 
                    A&amp;B groceries, which are smaller in size than supermarkets.
                    <br>
                    <br>
                    Superstores and supermarkets have the largest market share 
                    in Oman food retail. Many of them import directly, and have 
                    efficient logistics and upgraded storage means. They have 
                    more than 40 per cent of market share in Oman's food 
                    distribution. <br>
                    <br>
                    Many influences will affect the retail sector in the 
                    immediate future. In the next three years, as many as five 
                    new hypermarkets will open in Muscat. This is good news for 
                    consumers who will reap the benefit of retailers offering 
                    lower prices and incentives for their loyalty. However, the 
                    increased competition will lower the retailer's already thin 
                    margins. These new hypermarkets will not only compete 
                    amongst themselves, but with electronics, fashion and home 
                    ware retailers. <br>
                    <br>
                    Competition from Dubai is another consideration when 
                    planning for the future. Our customers are exposed to global 
                    retailers through travel, television and magazines. They are 
                    familiar with international brand names and will patronise 
                    them when possible. <br>
                    <br>
