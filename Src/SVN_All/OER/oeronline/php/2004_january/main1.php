                    <p align="justify"><font face="Verdana" size="1">On December 
                    9, 2003, OCIPED hosted a seminar on Economic Freedom 
                    organised by key representatives from The Fraser Institute 
                    and consultancy organisation Strategic Economic and 
                    Environmental Research Studies (SEERS). OCIPED invited 
                    guests from both private and public sector organisations to 
                    attend and to participate in workshops intended to improve 
                    their understanding of where Oman was positioned in relation 
                    to other nations' economic freedom and how the Sultanate 
                    could pave the way to improve its overall ranking. <br>
                    The Economic Freedom of the World (EFW) project is the 
                    culmination of a project dating back 15 years following a 
                    conversation between Michael Walker and Nobel laureate 
                    Milton Friedman. The question they raised was how economic 
                    freedom could be measured in order to objectively correlate 
                    it with supposed benefits. Several symposia and variants of 
                    the index have led to the 2003 EFW index. With a ranking for 
                    123 nations covering 91 per cent of the world's population, 
                    the EFW index is considered the most comprehensive of the 
                    various socio-economic indices produced today.<br>
                    As Nobel Laureate Douglass North has written, the measure of 
                    economic freedom provides the best description yet of 
                    efficient markets and it can become used as a map to guide 
                    economies to becoming more efficient and productive. The EFW 
                    index is made up of 38 parameters, broken down into five 
                    categories and several sub-categories. The five components 
                    lend equal weight to the overall index score. The key 
                    ingredients of economic freedom are personal choice, 
                    voluntary exchange, freedom to compete and protection of 
                    person and property. In particular, the EFW index measures 
                    the degree of economic freedom present in five major areas: 
                    size of government; legal structures and security of 
                    property rights; access to sound money; freedom to exchange 
                    with foreigners; and regulation of credit, labour and 
                    business. </font></p>
