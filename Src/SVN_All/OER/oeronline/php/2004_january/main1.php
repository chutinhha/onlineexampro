            <p align="justify"><font face="Verdana" size="1"><b>How well does 
            Oman score? </b><br>
            Oman's overall EFW score is 7.4. The result ranks the Sultanate as 
            the world's 18th most economically free country in the world, in 
            joint place with Belgium. This is a tremendous achievement and one 
            that is underlined by the fact that the Sultanate is deemed to be 
            the second most economically free nation in the Middle East. Hong 
            Kong clawed the first position in the index of economic freedom 
            while Singapore and the US were placed in second and third positions 
            respectively with New Zealand and the UK ranked joint fourth.<br>
            <br>
            In one particular area, that of 'access to sound money', Oman scores 
            9.7 which ranks it as the sixth most economically free nation in the 
            world in this category. There are four components to the index of 
            economic freedom in the area of sound money; average annual growth 
            of the money supply in the last five years, the standard inflation 
            variability in the last five years, the recent inflation rate and 
            the freedom to own foreign currency bank accounts domestically and 
            abroad. In order to earn a high rating in this area, a country must 
            follow policies and adopt institutions that lead to low (and stable) 
            rates of inflation and avoid regulations that limit the use of 
            alternative currencies should citizens want to use them. <br>
            <br>
            In this year's report, Sweden tops the ranking in the area of Sound 
            Money followed by the United States in second position and Taiwan in 
            third. The United Arab Emirates is ranked 39th in the world, India 
            88th and Russia 112th. <br>
            <br>
            Commenting on the results, His Excellency Hamoud Sangour Al 
            Zadjali,ÊExecutive President of the Central Bank of Oman, said, &quot;The 
            Central Bank of Oman is committed to excellence in promoting 
            monetary and financial stability and fostering a sound and 
            progressive financial sector, to achieve sustained economic growth 
            for the benefit of the nation. The Sultanate has a liberal system 
            that reflects its basic non-interventionist stance of policy in 
            economic matters relating to the production and exchange of goods 
            and services.&quot; <br>
            <br>
            &quot;Countries prosper when they establish an environment where 
            individuals can engage in productive activities and invest for the 
            future. Freeing people economically unleashes individual drive and 
            initiative and puts a nation on the road to economic growth. I am 
            please to see that the Sultanate's policies to create the conditions 
            are being recognised internationally,&quot; says Salem Ben Nasser Al 
            Ismaily, Executive President, OCIPED. <br>
            <br>
            On the success of the Sultanate in the EFW index, Neil Emerick, 
            Consultant, SEERS, says: &quot;The outlook for Oman's economy is very 
            positive. There seems to be a great willingness to embrace the 
            free-market ideology, which philosophically requires emphasis on 
            personal economic freedom. There is respect for this in government 
            tempered only by the specific Omani requirements - true of any small 
            nation - to maintain their culture and ways of life in an 
            increasingly global world. The degree to which the society is ready 
            for change will determine the pace of the economic reforms currently 
            underway.&quot; <br>
            <br>
            &quot;Oman has much to sing about to the rest of the world and perhaps 
            has been a little shy in doing so,&quot; he adds. <br>
            <br>
            Fred McMahon, Director of the Centre for Trade and Globalisation 
            Studies at The Fraser Institute, believes that Oman is well 
            positioned to take advantage of the opportunities economic freedom 
            promotes and create an even better future, saying, &quot;The Sultanate 
            can forge a future where Omani and foreigner alike will continue to 
            invest and create jobs, prosperity and profit.&quot; <br>
            <br>
            He concludes: &quot;Oman has already shown tremendous commitment to and 
            growth in economic freedom. It is not a far stretch for Oman to 
            reach world-class best practices. The work OCIPED is spearheading 
            can take the Sultanate past that threshold. That will not eliminate 
            all challenges or difficulties but is the best path forward.&quot; </font>
