            <font face="Verdana">Guiding words</font></font><font face="Verdana" size="1"><br>
            <b>Neil Emerick, Consultant at SEERS, gives OER his insights on how 
            the Sultanate measures up in the insights five key areas the EFW 
            index</b></font></p>
            <p align="justify"><b><font face="Verdana" size="1">
            <img border="0" src="pg23.jpg" align="right" hspace="5" width="167" height="199"></font></b><font face="Verdana" size="1"><b>1.) 
            Size of government: expenditures, taxes and enterprises <br>
            Oman's score: 5.8 - Rank 67th</b> <br>
            <b>Summary:</b> Oman has a large, interventionist government. Often, 
            when a nation is young, the dominant requirement is institutional 
            stability. This manifests itself in a large, omni-present state. 
            However, the need to 'deepen' the diversity of the economy, away 
            from energy products, and generate a more sustainable economic 
            environment for Omanis, is crucial if Oman is to maintain (and grow) 
            its relatively high standards of living. The sub-categories in this 
            area show Oman's real deficiencies. In Area 1A (Government 
            consumption as a share of total consumption), Oman scores 1.4, which 
            puts it 120th out of 123 countries. Some commentators argue that 
            Oman's government is too dominant in the economy, crowding out other 
            players and raising the price of capital to the private sector. <br>
            This is also seen in Area 1C (Government enterprises and investment 
            as a percentage of GDP). Oman scores 2.0 and ranks 84th out of 123. 
            The Omani government is both the biggest consumer and producer of 
            goods in the economy that means it decides what products and 
            services are produced in the economy rather than the satisfaction of 
            consumer demand by innovative entrepreneurs. <br>
            <br>
            <b>2) Legal structure and security of property rights <br>
            Oman score: 6.9 - Rank 31st <br>
            Summary: </b>In Area 2B (impartial courts) Oman scores a very 
            respectable 7.4 (21st) and in Area 2E (integrity of the legal 
            system) the country rates 19th with a score of 8.3. There are gaps 
            in the information for Area 2A (judicial independence) but in our 
            interviews we were not left with any impression that Oman scored 
            poorly in this regard. There were questions of general levels of 
            competence, but not suspicion. There are also gaps in Area 2D 
            (protection of intellectual property), but there is much academic 
            debate about whether scoring well in this regard necessarily 
            correlates with positive growth. <br>
            <br>
            <b>3) Access to sound money <br>
            Oman Score: 9.7 - Rank 6th <br>
            Summary: </b>Oman has thrown its monetary lot in with the US, 
            linking the convertibility of its currency to the US dollar, and 
            thereby sharing a similar EFW score with the US (9.8). In light of 
            the dollar reserves Oman generates from the sale of petroleum 
            products, this has proved to be a very effective policy. However, 
            considerations regarding a GCC currency, the possibility of floating 
            verses fixed exchange rates, the volatility of the US dollar, and 
            the possibility of linking the Rial to a basket of goods, all add to 
            the complexity of choosing suitable policy for Oman going forward. 
            Currently though, Oman is a world-leader in offering its citizens a 
            stable currency upon which economic planning can be carried out. <br>
            <br>
            <b>4.) Freedom to trade with foreigners<br>
            Oman Score: 7.9 - Rank 31st <br>
            Summary:</b> Area 4 has many parameters measuring trade, though the 
            score has equal weight in the overall index (20%). And again, Oman 
            has world-class figures. In Area 4A (Taxes on international trade) 
            Oman scores 7th with 9.4. In Area 4E (capital market controls), Oman 
            ranks 54th, which suggests some work. There is no score on 
            regulatory trade barriers, (Area 4B), which is where an economy can 
            hide much of its xenophobia, but overall Oman would appear to be 
            very much open for international business. <br>
            <br>
            <b>5.) Regulation of credit, labour and business <br>
            Oman Score: 6.6 - Rank 22nd <br>
            Summary: </b>There are gaps in the index for Oman in two of these 
            parameters, which could suggest further transparency in the 
            respective sectors is required. Generally, things look very 
            positive. In the credit section, Oman scores 8.9 (13th), as the 
            banking sector is reasonably well developed and sees little 
            interference (in terms of regulating interest rates) from central 
            government. There is competition from international banks and there 
            exists private ownership of domestic banks. The absence of 
            unionisation in Oman is a significant factor in developing a fluid 
            labour market, but this is hindered (in terms of economic freedom) 
            by the requirements of Omanisation. It is worth noting that the 
            government has potentially disarmed critics by allowing for 100 per 
            cent ownership in certain industries, and low Omanisation 
            requirements (10 per cent) in their corporate structures. However, 
            certain sectors are regulated by high requirements and inevitably 
            this will place rigidities in the economy. As the Omani economy 
            grows, there will be increasing pressure from a migrant labour force 
            entering the market, and how Omanis choose to deal with this will be 
            crucial to political stability. <br>
            <br>
            <b>Future overview</b> How can Oman improve its position in the EFW 
            index? Much depends on the Omani government redefining its role in 
            the economy and ultimately extricating itself as a major player. The 
            liberal market philosophy (supported by empirical evidence for 
            countries experiencing long term growth) requires governments only 
            to create and maintain the political institutions that handle rules 
            and arbitration for the exchange of property. <br>
            <br>
            It is the degree to which government adopts the role of referee - 
            rather than player - that determines the long-term prosperity of a 
            nation. The government is seen as the employer of first resort and 
            privileges relating to Omanisation place further strain on the 
            attractiveness of Oman as a destination for entrepreneurs. The 
            future wealth of young Omanis will depend on how successfully the 
            government is able to withdraw itself from economic activity, 
            encouraging in its stead industrious and creative individuals.</font></p>
