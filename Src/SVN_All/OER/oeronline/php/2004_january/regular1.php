            <font face="Verdana" size="4" color="#FF0000">M&amp;A: Back to basics</font><p align="justify">
            <b>
                  <font face="Verdana" size="1">Mergers seem 
            to generate net economies of scale, but they are also costly. There 
            are good acquisitions and bad ones too, says Ali Hassan Moosa</font></b><br>
                  <br>
                  <font face="Verdana" size="1">
            <img border="0" src="pg74.jpg" align="left" hspace="5" width="175" height="210">The 
            financial services industry across the globe has undergone a 
            significant transformation and restructuring in the last two 
            decades, leading to a number of mergers and acquisitions. Locally, 
            regulators have encouraged such moves in order to support the 
            creation of a robust and vibrant financial services sector that is 
            ready to face the new challenges, threats and opportunities of the 
            GCC financial integration and the WTO (World Trade Organisation) 
            regime. It is believed by a number of experts that the Sultanate's 
            financial industry should continue efforts of mergers and 
            consolidations as means of a fast-track defensive, or arguably, 
            aggressive market positioning in the face of increasing regional and 
            international competition. <br>
            <br>
            The developed financial markets of the 
            West had hundreds of mergers in the last few years from smaller 
            financial bodies to larger conglomerates. The amalgamation process 
            included cross-border entities. One of the largest mergers in the 
            recent history took place around five years ago between Citicorp and Travelers, creating Citigroup with assets in excess of $700 billion 
            (RO269 billion) and a market capitalisation of $44 billion (RO17 
            billion) at the time, making it the largest financial services 
            company in the world. Travelers itself was the creation of a number 
            of mergers and acquisitions between the originally formed Travelers 
            and Primerica (and Smith Barney) and later on between Travelers and 
            Salomon Bros. <br>
            <br>
            The primary objective of the deal is to enhance 
            shareholder value by means of a merger, acquisition or a 
            consolidation. A merger is an arrangement between two financial 
            entities in which only one would retain its identity. An acquisition 
            is the purchase of assets of another financial entity in order to 
            control its interests. A consolidation is a combination of two or 
            more financial entities creating a new identity. <br>
            <br>
            Further definitions 
            of mergers exist. A merger could be categorised as horizontal if the 
            two firms are in the same line of business. A vertical merger is one 
            in which the acquirer expands backward to secure supplies or forward 
            in the direction of the consumer. A conglomerate merger involves 
            companies in unrelated lines of business. <br>
            <br>
            Leverage buy-outs (LBOs) 
            differ from normal acquisitions as the purchase activity is financed 
            through debt, and the shares of an LBO are no longer traded in the 
            open market where it would be privately held by a group of 
            investors. Should this investor group be the management of the 
            company, then the correct term for the LBO would be Management 
            buy-out (MBO). <br>
            <br>
            Mergers are means to achieve rapid asset growth, to 
            enter a new market, to deter competitor entry into an existing 
            market and to add new lines of products and services. But to create 
            that shareholder value, synergy must exist. Contrary to the belief 
            that size does matter, conglomerate mergers may not necessarily 
            enhance shareholder value. Having said that, smaller banks may wish 
            to reach a critical mass in a market where size is the basis of 
            success. <br>
            <br>
            An acquisition may not be attractive if, for instance, 
            investors are concerned that the acquirer may not earn from the 
            expected investment in a reasonable time frame. One way to quantify 
            it is through earnings per share (EPS), which should not produce 
            dilution for the acquirer greater than an acceptable percentage 
            (e.g., a maximim of 5 per cent).<br>
            <br>
            Most acquisitions have a negative 
            short-term effect on earnings because the acquiring bank pays a 
            premium for the takeover bid. The decline in EPS should be small 
            enough and relatively short-lived in order for the deal to be 
            attractive to investors. One of the problems witnessed in 
            unsuccessful mergers from an investor's perspective is where EPS 
            dilution is excessive, resulting in the purchase price being so high 
            that it takes a long time for the acquiring bank to improve earnings 
            and performance. <br>
            <br>
            Mergers are often awkward transactions to evaluate. 
            The transaction itself is treated as a pure investment and, thus, 
            the appropriate method to evaluate the investment is to discount the 
            expected future cash flows from the newly created entity at a 
            reasonable rate. <br>
            <br>
            The Net-Present-Value (NPV) approach is far more 
            reliable than other methods of evaluating the transaction price such 
            as book value that may not reflect the firm's true economic value. 
            However, there are a number of experts in this field that believe 
            that an adjusted book value analysis does provide a useful mechanism 
            to evaluate the price. Adjusting the book value may be through a 
            number of means - mainly, by taking into account some factors that 
            include loan loss provisions, current market value of investment 
            securities and off-balance sheet activities. <br>
            <br>
            There are other 
            ingredients into the equation that are non-financial. It is believed 
            that many potential mergers were blocked due to the egos of members 
            of the board of directors and/or senior management. It is natural 
            that the seller company's directors/management may be threatened 
            that the acquiring firm will gain control at their expense. Before 
            the actual merger, it is important to devise a strategy to retain 
            key people. Any loss of key personnel on either end makes it 
            difficult to grow earnings and could damage the image of the 
            consolidated entity. The essential point is that uncertainty creates 
            immense anxiety among employees, senior management and directors. 
            <br>
            <br>
            Hostile takeovers do require offensive/ defensive tactics and 
            strategies. When the seller opposes the acquisition, one of the most 
            widely known takeover tactics is a tender offer. The acquirer 
            publicly offers seller's shareholders a purchase offer for a certain 
            number of shares in return for cash, securities or a combination of 
            both. Such tender offers are accepted or rejected by the seller's 
            shareholders at an Extraordinary General Meeting (EGM). <br>
            <br>
            On the other 
            hand, a good defence against a hostile takeover is to change company 
            charter (Memorandum of Association) to make unwanted takeovers 
            difficult. This process may be time consuming as it requires the 
            approval of various government departments including regulators as 
            well as shareholders. <br>
            <br>
            Corporate governance and transparency code of 
            conduct for publicly listed companies dictate that companies 
            promptly make a market declaration if there are any potential merger 
            activities even if they are at an early stage of discussions. Such 
            guidelines have been put in place to protect investors and to 
            minimise the risk of insider trading. <br>
            <br>
            The greatest concern to 
            authorities is that the merged entity must not gain control over the 
            market to pursue monopolistic pricing and output policies. 
            Therefore, large mergers could be blocked by governments in order to 
            protect consumers. <br>
            <br>
            Mergers seem to generate net economies of scale, 
            but they are also costly. There are good acquisitions and bad ones 
            too, but economists find it hard to agree whether acquisitions are 
            on balance beneficial. A merger does generate substantial gains to 
            the acquired firm's stockholders even if the buyer may only break 
            even. Some believe that investors pay too much attention to the 
            short-term earnings gains and do not notice that these gains are at 
            the expense of long-term prospects. <br>
