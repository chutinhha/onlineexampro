                    Let me consider the opportunities and threats to media in 
                    Oman and the Gulf region. <br>
                    <br>
                    <b>a) Consumers </b><br>
                    There is a large shift in population patterns in the Gulf 
                    with young population growing in numbers and influence in 
                    society. As these new generations grow through new life 
                    stages, their consumption patterns as well as their outlook 
                    and values will have a major impact on their respective 
                    societies. <br>
                    <b>i) Opportunity:</b> New ideas and attitudes. More 
                    consumer spending on products and services. More female 
                    involvement in decision-making, especially true for Oman.
                    <br>
                    <b>ii) Threat:</b> Less newspaper reading. Worldwide, the 
                    younger generation reads far less. Media organisations are 
                    looking for ways to hold this segment. <br>
                    <br>
                    <b>b) Advertising Customers</b> <br>
                    Media Buying and Planning shifting to specialised agencies.<br>
                    <b>i) Opportunity:</b> Media houses can develop 
                    relationships with consumer segments and develop insights 
                    into their lifestyles and habits. Thus they can encourage 
                    value-based buying. <br>
                    <b>ii) Threat:</b> There is the danger of media becoming a 
                    commodity with price-based buying and increasing reliance on 
                    databases cutting out well-established buyer-seller 
                    relationships in advertising. <br>
                    <br>
                    </font><font face="Verdana" size="2"><b>Media Convergence</b> </font><font face="Verdana" size="1">
