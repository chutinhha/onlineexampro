                    to other regions of the world with similar per-capita GDP.
                    <br>
                    <b>a. Impact on Consumer Base </b><br>
                    Few traditional consumers have been lost to Internet usage, 
                    but there is no cause for complacency in traditional media 
                    houses. There is a huge segment of the youth market that has 
                    never used old media and can potentially start their media 
                    experience through the Internet. <br>
                    <b>b. Impact on Customer Base </b><br>
                    Internet advertising has had no impact on advertising in 
                    traditional media and the share of new media in advertising 
                    budgets is miniscule. <br>
                    <br>
