                    <font face="Verdana" size="1"><b>Crystal Gazing </b><br>
                    Any effort to predict the future has only one certainty - 
                    the forecaster is sure to be at least partly wrong. I will, 
                    nevertheless, venture into discussing the future of the 
                    media industry. <br>
                    <br>
                    Media consumers include readers, viewers and users of media 
                    in various forms. For the sake of convenience, I will call 
                    them &quot;consumers&quot;. These consumers look at media as a source 
                    of information and entertainment. <br>
                    <br>
                    Media companies generate influence from their reach among 
                    consumers. Media is also an industry, generating revenue. 
                    This revenue comes mainly from advertisers, but can also 
                    include circulation/subscription revenue and other revenue. 
                    I will group all sources of media revenue together and label 
                    them &quot;customers&quot;. <br>
                    <br>
                    The influence that customers hope to have on their target 
                    audiences through any particular medium is only a function 
                    of the reach and influence that the medium itself has. As 
                    the relationship between customer and the medium progresses, 
                    the degree of influence becomes apparent and, ultimately, 
                    affects the revenue share allotted. Both dimensions of 
                    influence and revenue can thus be merged into one for this 
                    crystal gazing exercise. <br>
                    <br>
                    The media industry in Oman must be viewed as an industry, 
                    capable of generating revenue. In terms of Oman's national 
                    priorities, this industry is also a source of export 
                    earnings from overseas advertisers. <br>
                    <br>
                    In the chart, it is apparent that Oman has about 8 per cent 
                    share of per capita GDP in the GCC, but only a 3 per cent 
                    share of advertising spend. This 5 per cent gap is due to 
                    poor international perceptions of Oman's viability as a 
                    marketplace. Over the last two years, this perception is 
                    being corrected and figures for the first half of 2003 
                    indicate that Oman had the highest percentage growth of ad 
                    spend in the GCC. This process can generate substantial 
                    extra revenue for Oman's media and fuel further development 
                    in reaching the consumers. <br>
                    <br>
                    </font><b><font face="Verdana" size="2">Opportunities and 
                    Threats</font><font face="Verdana" size="1"> </font></b><font face="Verdana" size="1">
