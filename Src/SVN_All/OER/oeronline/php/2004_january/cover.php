                    <p align="justify"><font size="4" color="#FF0000">
                    <font face="Verdana">Forecast</font></font><font face="Verdana" size="1"> </font>
                    <font size="4" color="#FF0000"><font face="Verdana">2004</font></font><font face="Verdana" size="1"><br>
                    Who'd be a forecaster? 
                    These are unpredictable global times and the past few years 
                    have produced the most uncertain economic outlooks for 
                    decades. It's clear that economic forecasting always 
                    involves a large element of crystal ball gazing yet to plan 
                    for the future it is essential to form a firm view of what 
                    lies ahead. Obviously the economy of Oman does not operate 
                    in isolation and it will be heavily influenced by changes at 
                    a regional and, indeed, global level. It is evident that oil 
                    prices will be of vital importance to consider and forecast. 
                    Oil traders joke that forecasts are written in sand, not 
                    stone. Uncertainty concerning potential oil price outcomes 
                    over the next four quarters still looms large.<br>
                    <br>
                    Therefore, the comments of the forecasters we have brought 
                    together are particularly interesting given the lack of 
                    analysts which are eager at this moment to give any sort of 
                    prediction on which direction energy markets will follow 
                    among other fundamentals.<br>
                    <br>
                    <b>Primary aim</b> <br>
                    The strategic objective of running this series of forecasts 
                    in OER is to assist our readers to gain a better 
                    understanding of what kind of economic activity Oman can 
                    expect in 2004 and beyond. The national picture is one of 
                    increasing competition for sales and market share along with 
                    increasing pressure on companies to cut costs and innovate. 
                    It is encouraging to note that all of our crystal ball 
                    gazers predict that Oman's business community will rise to 
                    the challenge. However, while most have a strong belief that 
                    the economy will be essentially stable and improving, a few 
                    remain sceptical that the progress that will be achieved in 
                    their respective market segments next year will be as 
                    substantial as many are hoping for. <br>
                    <br>
                    <b>Vive le difference </b><br>
                    How is this analysis of the possible future different from 
                    the run-of-the-mill ones that you may have seen before or 
                    those that will follow? Firstly, we have begun with a global 
                    economic overview followed by a series of meaningful local 
                    perspectives. Thus, our readership is assured of a rounded 
                    and well-crafted presentation. <br>
                    <br>
                    We also believe that more care and consideration has been 
                    given to finding suitable candidates to give predictions. 
                    There is a fair sprinkling of forecasts provided by 
                    government officials: You will be pleased to find out that 
                    two of Oman's Ministers and one of the country's 
                    Undersecretary's are represented as well as other top 
                    ranking civil servants. If you take a look at the private 
                    sector participation, OER has also invited top-notch market 
                    leaders to take part, not mediocre soothsayers. Therefore, 
                    you will be equally excited to learn that the likes of Saud 
                    Bahwan have got involved as well as other leading business 
                    people. <br>
                    <br>
                    <b>Future focus</b><br>
                    Moreover, less time is devoted to dwelling on the events of 
                    2003 (after all this is what OER covered in-depth in its 
                    past issue) and a clear brief has been given to each of our 
                    experts to focus on defining the evolution of their 
                    particular market sector and to comment on emerging trends 
                    as well as to anticipate new economic circumstances. <br>
                    <br>
                    After their deliberations, cogitations and procrastinations, 
                    we hope you enjoy their visions of the road ahead for 
                    corporate Oman. Perhaps as 2004 draws to a close, we can 
                    reflect on how many of these predictions came true. <br>
                    <br>
                    The industry leaders' forecasts - in alphabetical order - 
                    follow the national economic overview provided by HE Ahmed 
                    Bin Abdulnabi Macki, the Minister of National Economy, and 
                    is concluded by the global economic outlook by David Bloom, 
                    Director (Strategy), Global Markets, HSBC Bank, London.</font><font size="1"> </font></p>
