                    there is no sign of any slackening in the pace of Oman’s 
                    legislative development. In 2004 it is anticipated that we 
                    shall see a new Commercial Companies Law, a new Advocacy 
                    Law, changes to the Commercial Agencies Law and, possibly, 
                    even a law to permit foreigners to own property in Oman. <br>
                    <br>
