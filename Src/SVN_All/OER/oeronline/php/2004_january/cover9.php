                    <img border="0" src="cover8.jpg" align="left" hspace="5" width="171" height="192">First 
                    came the lawyers, then came the rules…, or so sang Dire 
                    Straits in their classic rock ballad Tobacco Road. As a 
                    lawyer I would, of course, refute the implicit suggestion 
                    they make that lawyers, and “the rules”, are an unnecessary 
                    component of the human condition; and the evidence provided 
                    by Oman’s recent legislative developments (and by “recent” I 
                    mean the last thirty years or so) proves my point. <br>
                    <br>
                    Law is often described as the oldest (or, more scurrilously, 
                    the second oldest) profession. Its origins are undoubtedly 
                    ancient. No one can say for sure when the first man-made 
                    laws were crafted, but it is reasonable to suppose that they 
                    were drawn-up in the Middle East, which is widely recognised 
                    as the cradle which is widely recognised as the cradle of 
                    civilisation. What is certain is that the origin of law in 
                    what is today Oman goes back to the very earliest recorded 
                    times.<br>
                    <br>
                    That law developed and changed with the political and 
                    military fortunes of the various tribes, empires, and 
                    peoples which occupied this land. Shortly after the advent 
                    of Islam in AD 570, Islamic law was introduced to this 
                    region and has formed the basis of the law here ever since.
                    <br>
                    <br>
                    The existence of the Ottomans, Portuguese and British in the 
                    region left relatively little imprint on Oman or its laws, 
                    largely because, unlike other Gulf states, Oman existed as 
                    an independent state from at least 1650 (Kuwait, Bahrain, 
                    Qatar and UAE became independent of British rule only in 
                    1960 and 1971 respectively). <br>
                    <br>
                    Yet until the accession of His Majesty Sultan Qaboos, the 
                    development of Omani law differed little from the pattern 
                    seen elsewhere in the Gulf region. Like other Gulf states, 
                    Omani laws were influenced by Egyptian law, the great 
                    classical Egyptian commentators such as Sanhouri, and the 
                    Egyptian Civil Code. However, since 1970, Oman’s laws have 
                    been transformed. It is not an overstatement to say that 
                    Oman is in the vanguard of Gulf states in introducing laws 
                    designed to encourage privatisation and inward investment. 
                    As a result, Oman’s privatisation program is the most 
                    advanced in the Gulf. <br>
                    <br>
                    Whilst others talk interminably about extending equal 
                    treatment to GCC companies and individuals, Oman has 
                    actually done it, and those in neighbouring GCC countries 
                    have already taken advantage of relaxed rules which permit 
                    the establishment of 100 percent foreign (GCC) owned 
                    companies in Oman to invest in Oman.<br>
                    <br>
                    Although businessmen often assert that Dubai is more 
                    business-friendly, international lawyers generally 
                    acknowledge that the legal framework in Oman is the most 
                    developed in the Gulf. In the last few years, there has been 
                    a root and branch reorganisation of the Omani court system, 
                    a new Labour Law, new Codes of Corporate Governance, new 
                    Capital Market Authority Circulars affecting public joint 
                    stock companies, and some important changes to the Tax Law 
                    and the Commercial Companies Law.<br>
                    <br>
