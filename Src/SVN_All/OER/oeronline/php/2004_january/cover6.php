                    2004 business plans come immediately into effect many in 
                    corporate Oman who will have confidently set targets to 
                    improve on their 2003 financial performance in Oman's ever 
                    thriving and growing economy. The future indeed looks rosy, 
                    and some of you will undoubtedly feel that just modifying 
                    existing business models focused on the financial bottom 
                    line will ensure increased growth in 2004. You will probably 
                    be right, but looking further into the future is your 
                    business able to meet the challenges of the Oman economic 
                    vision of 2020? In fact will your business survive to 2020? 
                    Have you thought that far ahead? One thing is for certain 
                    your business model of 2003 will not guarantee indefinite 
                    success. The key to future success is sustainable business.
                    <br>
                    <br>
                    The Brundtland Report, presented as part of the Rio Earth 
                    Summit of 1992, provided the well-known definition of 
                    sustainable development as &quot;development that meets the needs 
                    of the present world without compromising the ability of 
                    future generations to meet their own needs&quot;. Sustainable 
                    development was thus born as a globally recognised term and 
                    its vision led the way to achieving a brighter future for 
                    our continued industrialised existence on this planet. 
                    Despite promising much sustainable development failed to 
                    engage with business its primary target audience. Seen 
                    solely as a green agenda by many in business the maiden 
                    voyage of sustainable development foundered against the 
                    rocks of implementation. Still, regulators, NGO's and 
                    enlightened business continued to build and define 
                    sustainable development and important breakthroughs were 
                    made. <br>
                    <br>
                    It wasn't until later in the 1990s that sustainability began 
                    to firmly establish itself on the business agenda. Global 
                    business in its relentless pursuit of increased shareholder 
                    value at almost any cost, received high profile 
                    international media attention reporting alleged human rights 
                    and environmental abuses. Many global companies fell foul to 
                    these campaigns, which ultimately led to loss of sales, 
                    costly lawsuits and damage to brand and reputation. Milton 
                    Friedman's well accepted definition that, &quot;the 
                    responsibility of business is exclusively to maximise profit 
                    for shareholders&quot; all of a sudden began to look incomplete. 
                    Business awakened to the realisation that integrating social 
                    and environmental aspects into core management was critical 
                    for financial success and indeed survival. <br>
                    <br>
                    The UK's John Elkington, a leading edge thinker, defined 
                    sustainability as the &quot;principal of ensuring that our 
                    actions today do not limit the range of economic, social, 
                    and environmental options open to future generations&quot;. The 
                    realisation of the interdependence of social, environmental 
                    and economic issues led to the triple bottom line as an 
                    alternative to the corporate financial bottom line. The 
                    triple bottom line has proven to be a tremendously useful 
                    and popular concept. It has led many companies to prepare 
                    annual corporate reports designed to add assurance to 
                    shareholders, stakeholders and society that a company is a 
                    good corporate citizen and conducts its business ethically.
                    <br>
                    <br>
                    The new economy born out of the dot com boom radically 
                    changed the way business interacts with its employees, 
                    suppliers, customers and society. New economy thinking 
                    fathered a new social revolution with IT and sustainability 
                    at its core and new opportunities and threats for business 
                    to manage. Thus, the very concept of sustainability was 
                    revitalised by new economy creative thinkers who placed 
                    employee and social networks, employee empowerment, ideas 
                    creation, innovation and wealth generation at the core of a 
                    sustainable business. <br>
                    <br>
                    Sustainability is here to stay. Future success will 
                    increasingly be governed by business's that succeed in 
                    taking the opportunities and benefits presented by the 
                    sustainability challenge, which include:<br>
