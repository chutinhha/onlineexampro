                    <p align="justify"><font face="Verdana" size="1">As the 
                    colour and flavour of the labour market begins to change 
                    with the increased number of Omanis in the job market, so 
                    will the need for mindset change on the part of companies' 
                    senior managements. Managing a growing local workforce 
                    requires new strategies and a new set of tools. <br>
                    <br>
                    In 2004, we will see the need to sharpen the effectiveness 
                    of HRD programs, HR Management Skills and Employment 
                    Practices. In fact, the Oil &amp; Gas sector has already started 
                    doing that. In 2003, Producers &amp; Operators adopted the OPAL 
                    Compliance Verification Certificate, commonly known as the 
                    OPAL Mandate, designed to help maturing companies embrace 
                    appropriate employment practices and attain fair business 
                    environment for all. <br>
                    <br>
                    The legislation of the new Minimum Wages, with effective 
                    date set for January 1, 2005, will add a new curve to the 
                    existing challenges. In 2004, we will witness increased 
                    attention being devoted to the proper implementation of HR 
                    Management Systems and particularly towards jobs 
                    categorisation and classification, jobs description and 
                    salary structures. <br>
                    <br>
                    With the new Labour Law permitting the formation of 
                    professional and labour associations or societies, we may 
                    witness in the last quarter of 2004 an increase in the 
                    number of such associations. This also enhances the 
                    importance of sharpening HRM skills of all managers in an 
                    organisation and strengthening the view that HR development 
                    and management is everybody's responsibility within an 
                    organisation. <br>
                    <br>
                    We will also witness the unified approach of the government 
                    in making quality Omanisation and local workforce 
                    development the prime priority in assessing the social 
                    benefits of government-funded projects, particularly the 
                    ones offered for tender. All of these will create the need 
                    for managers to source out specific services such as 
                    Competency Assessment &amp; Assurance as well as psychometric 
                    tests to help them evaluate the productivity of their staff 
                    as well as their potentiality for development. OPAL has 
                    already started in the last quarter of 2003 to give 
                    attention to these tools for the benefit of its members and 
                    the Oil &amp; Gas sector as a whole. <br>
                    <br>
                    Companies will scramble to attract the best resources in the 
                    coming years. Hence, senior management will be devoting more 
                    attention to proper recruitment and improved employment 
                    practices. These are the key elements needed to acquire and 
                    retain the appropriate resources in the organisation. The 
                    need for highly qualified Omani HR managers will increase in 
                    2004 as well as for Training and Personnel managers.</font></p>
