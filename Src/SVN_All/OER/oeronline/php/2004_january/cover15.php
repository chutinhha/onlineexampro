                    the local university student community on ‘know your stock 
                    market’ and ‘smart-investor’ systems. The CMA has signed an 
                    Memorandum of Understandings (MoU) with the Malaysian 
                    Securities &amp; Exchange Commission which is only a formal 
                    agreement of the existing understanding and cooperation 
                    between the two organisations. The exchange has also signed 
                    a MoU with the Kuala Lumpur Stock Exchange. The agreement is 
                    expected to explore possibilities of strategic alliance 
                    between the two exchanges, which include cross listing of 
                    securities in future.<br>
                    <br>
