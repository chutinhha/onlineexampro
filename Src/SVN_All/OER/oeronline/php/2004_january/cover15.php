                    <img border="0" src="cover15.jpg" align="left" hspace="5" width="200" height="276">The Muscat Securities Market (MSM) 
                    is celebrating its 15th anniversary this year after being 
                    set up to play a crucial role in directing savings towards 
                    investment and providing the economy with funds for new 
                    ventures and developmental projects. Since the second half 
                    of the 1990s, the MSM has been maintaining and ensuring a 
                    fair and open price formation providing shareholders and 
                    investors with reliable information. <br>
                    <br>
                    The MSM is expected to remain bullish in 2004 after 
                    witnessing a steady performance during the current year, 
                    which is drawing to a close. The reason for the buoyancy in 
                    the capital market can be attributed to the upbeat economy, 
                    which is riding on higher oil prices and low inflation. 
                    Increased activities in the non-oil sector and rise in LNG 
                    production and exports have also contributed to the economic 
                    growth. With oil production expected to average around 
                    800,000 barrels per day by the end of December and the oil 
                    prices remaining at the present peak levels, the economy is 
                    expected to maintain its growth, which will be reflected in 
                    the capital market as well. <br>
                    <br>
                    During 2003, an association of stockbrokers came into 
                    existence to ensure fair and honest trade practices. The 
                    association, which is established in accordance with Article 
                    32 of the Omani Capital Market Law, is expected to safeguard 
                    the adherence of justice, integrity and efficiency of Oman’s 
                    brokerage business. As a step towards enhancing disclosures 
                    and transparency, the exchange started disclosing the 
                    transactions of the members of board of directors on their 
                    companies’ shares. This is being done through the electronic 
                    trading system. <br>
                    <br>
                    The CMA and the MSM had always been exploring crucial issues 
                    such as corporate governance, shareholder rights, and timely 
                    disclosure among other matters. The CMA has been successful 
                    in implementing disclosure norms, which has restored 
                    investor confidence to a great extent in the recent past. 
                    CMA’s efforts are very clear and are not only meant to 
                    strengthen the country’s stock market system, but also to 
                    enhance the efficiency of corporate mechanism, which governs 
                    public limited companies. The CMA is also constantly 
                    reviewing existing guidelines with a view to encouraging a 
                    culture of corporate self-regulation. <br>
                    <br>
                    There are several rules and regulations introduced by CMA 
                    that require companies listed on the MSM to be transparent 
                    and disclose immediately any significant information that 
                    may affect the prices of the shares. If companies failed to 
                    comply with these measures, the CMA could take action 
                    against these non-compliant companies. The MSM is 
                    continually cleaning-up its two active segments - regular 
                    and parallel markets - that can give face-lift to the 
                    General Price Index and make it a real indicator of market 
                    situation. The restructuring is meant to ensure a fair, 
                    orderly, honest and a transparent stock market. The 
                    re-structuring takes place after the MSM x-raying on key 
                    factors such as profitability, capital and shareholders’ 
                    funds, return on equity, the net profit margin, liquidity, 
                    assets and liabilities etc. <br>
                    <br>
