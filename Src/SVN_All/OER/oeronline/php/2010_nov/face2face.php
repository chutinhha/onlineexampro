                  <p align="justify" style="margin: 3px 10px"> 
					<b><font face="Verdana" size="1" color="#C00000">&nbsp;<br>
					</font><font face="Verdana" size="4" color="#C00000">An 
					integrated approach</font></b><br>
					<font face="Verdana" size="2"><b><br>
					</b>
					</font><font face="Verdana" size="1"><br>
					<b>
					<img border="0" src="images/nov_face2face1.jpg" width="150" height="210" align="left" style="border: 1px solid #808080" hspace="7"></b></font><b><font face="Verdana" size="2">William 
					R Crew, General Manager, GroFin/Intilaaqah shares his views 
					on promoting entrepreneurship and the challenge posed by 
					ingrained mindsets with Mayank Singh in an exclusive 
					interview. Excerpts:</font></b><font face="Verdana" size="2"><br>
					</font><font face="Verdana" size="1"><br>
					<font color="#000080"><b>You have had prior work experience 
					in SME financing in Oman, what made you come back to the 
					Sultanate?</b></font><br>
					<br>
					I am very keen on small business finance and on Oman. I 
					enjoyed my time here and found the country and people to be 
					very hospitable towards business. I have been working on 
					small businesses throughout my life. I worked in the US as a 
					member of the credit administration of the largest SME 
					lender in the country, and then overseas mostly in Europe, 
					Middle East and parts of the former Soviet Union. All of 
					these assignments have been with regards to small business 
					lending with banks and non banking finance companies. I have 
					worked as a consultant for a US consultancy group which 
					provided services to the European Bank for Reconstruction 
					and Development (EBRD) on various projects. I would go into 
					a bank which had EBRD financing and set up a loan programme, 
					train loan officers, look at the credit risk, manage the 
					procedures and change these. I studied finance and have an 
					MBA from the University of Chicago, US. GroFin is the fund 
					manager for Intilaaqah and it manages the funds that Shell 
					has given to the Sultanate of Oman. Shell gave $10mn in 2005 
					and that was when GroFin based in South Africa was hired to 
					manage the fund. Our mission is to create jobs in Oman and 
					we do that by strengthening the SME market in Oman and by 
					making loans available to small businesses in Oman. But it 
					is more than money as Intilaaqah also gives business 
					development support. So it is an integrated package of 
					finance and advisory solutions. <br>
					<br>
					<font color="#000080"><b>You were a member of the team that 
					helped set up Al Wathbah, the SME lending arm of BankMuscat. 
					What are your views on SME financing in Oman?<br>
					</b></font><br>
					The support being given to small businesses in Oman is very 
					impressive and BankMuscat has been a leader in developing a 
					platform that is sensitive to small businesses and they are 
					very serious about growing small businesses in the 
					Sultanate. The bank did a lot of research in the market and 
					tailored products to suit the needs of small businesses.<br>
					<br>
					<font color="#000080"><b>What are your plans for Oman and 
					what else are you planning to do that has not been done as 
					yet?</b></font><br>
					<br>
					These are exciting times as Shell has given an additional 
					$7mn gift to the nation and with that we are integrating 
					Intilaaqah’s training programme and the investment fund. 
					This advisory services will enable us to bring the funding, 
					advisory services, and SME training under the same roof. 
					Historically the training has been classroom training but 
					now it is going to be online and faster. The training will 
					continue to be offered to all SMEs in general, even those 
					who do not received financing from the fund. Thus we are 
					making the package bigger with Shell’s recent offering to 
					the nation.<br>
					<br>
					<font color="#000080"><b>What is the nature of the money 
					given by Intilaaqah to SMEs, is it a loan or a grant? </b>
					</font><br>
					<br>
					It is a loan on commercial terms. However, it is much more 
					than money. Intilaaqah advisory and training helps Omani 
					entrepreneurs and SMEs to continue being successful and 
					profitable. <br>
					<br>
					<font color="#000080"><b>How much is the difference between 
					the loan rates between a commercial bank and the loan given 
					by Intilaaqah?</b></font><br>
					<br>
					Our financing is based upon cashflow, not collateral. Hence 
					we take on a bit more risk than the normal commercial bank. 
					This increase in risk and the additional advisory services 
					price Intilaaqah somewhat above the commercial SME rate for 
					a loan which has collateral coverage of 100-200 per cent, 
					that is, a secured bank loan. In the case of banks there is 
					always going to be an annual fee, a statement fee and like a 
					bank we also charge a fee for our services; it is hard to 
					pinpoint a number, but we are looking into that now. A bank 
					is always going to want real estate as collateral, but we 
					look at cash flow as it allows the entrepreneur to run his 
					business. In terms of collateral we take personal 
					guarantees, a pledge against business assets and also real 
					estate but we do not take 100 per cent coverage. So that’s 
					how we compete with banks.<br>
					<br>
					<font color="#000080"><b>In any country, SMEs are the main 
					job creators. Do you think that this model can be replicated 
					in Oman?</b></font><br>
					<br>
					Entrepreneurship is the way to go and Oman is doing a lot to 
					support that. I recently read a study which suggested that 
					families in the GCC prefer that their children work for the 
					government versus starting their own business and this is 
					understandable as around 75 per cent of all businesses fail 
					in three years globally. So this may be a reason as to why 
					families want their children to go into the government 
					sector or large corporations. The challenge is to change 
					perceptions about what it means to be an entrepreneur. <br>
					<br>
					<font color="#000080"><b>Your corpus has doubled, and a 
					certain amount of awareness about entrepreneurship has been 
					generated in Oman. How do you plan to take this forward?</b></font><br>
					<br>
					Our advisory service and Intilaaqah training programme are a 
					service in itself and because of this we increase the 
					probability of success of a business. My message to the 
					market is, working with us decreases the probability of 
					failure. In the US many presidents have failed multiple 
					times but they have still gone on to become presidents. If 
					they had stopped with one failure they would not have 
					succeeded. Oman too would be having many such stories and it 
					is important to highlight such stories as we move forward 
					with the new Intilaaqah. <br>
					<br>
					<font color="#000080"><b>What kind of handholding would you 
					be doing with these entrepreneurs?</b></font><br>
					<br>
					We monitor the financial health of every client regularly. 
					They submit to us their management account on a monthly 
					basis and audited accounts on an annual basis. We look for 
					business risks, for example if the account receivables are 
					increasing then it means that clients are not receiving 
					debtor payments in a timely manner. So we will sit down with 
					our clients to analyse these risks and to come up with a 
					plan to mitigate them. I was in the interiors this week and 
					found a client who was facing a lot of challenges, so we sat 
					with him and looked at his marketing plan. He is a good 
					entrepreneur but as they say two heads are better than one, 
					so we are in a position to work as an outside consultant. We 
					may be in a position to see things a bit differently than 
					the way that an entrepreneur may see them and together we 
					may be able to come up with a few new ideas. We also make 
					daily connections between our clients and people whom we 
					know using our network. <br>
					<br>
					<font color="#000080"><b>On what basis do you decide to lend 
					money to clients. Is it their business plan or the spark 
					that you see in them etc?</b></font><br>
					<br>
					As stated we always look at the ability of a business to 
					generate cash and we judge that ability, by looking at the 
					entrepreneur. The No. 1 criteria is the entrepreneur, his or 
					her managerial experience, commitment to the business and 
					this is more experience versus education. We always look at 
					a CV as past experience gives us an idea as to whether an 
					individual can manage his business and create enough cash 
					flow to pay Intilaaqah, suppliers and to take money out for 
					his family. <br>
