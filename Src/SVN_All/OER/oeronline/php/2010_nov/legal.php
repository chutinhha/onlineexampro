					<b><font face="Verdana" size="1" color="#C00000">&nbsp;</font><font face="Verdana" size="4" color="#C00000"><br>
					<br>
					Financing structures<br>
					</font><font face="Verdana" size="2"><br>
					Any shares not subscribed to by a shareholder may only be 
					subscribed by other shareholders of the company in 
					proportion to shares owned by each of them<br>
					<br>
					</font><font face="Verdana" size="4" color="#C00000">
				<img border="0" src="images/nov_legal1.jpg" hspace="9" vspace="5" style="border: 2px solid #C0C0C0; " align="left" width="200" height="230"></font></b><font face="Verdana" size="1">Acompany 
					looking to strengthen its cash position can either opt to 
					enhance its capital base through an equity raising or it can 
					satisfy its funding needs through debt financing. Each of 
					equity funding and debt financing can be structured in 
					different ways. The most common ways are described below.<br>
					The obvious distinction between equity funding and debt 
					financing is that equity investors put in cash in exchange 
					for shares in the company (partial ownership) and receive a 
					dividend as a return on their investment if the company 
					generates profits and adequate reserves have been put aside. 
					It is ultimately the majority shareholders’ prerogative to 
					decide whether a dividend is to be distributed to all 
					shareholders pro rata to their equity participation in the 
					company or whether profits are to be retained within the 
					company for working capital needs or future capital 
					expenditure or otherwise as decided by the management of the 
					company. <br>
					<br>
					Debt investors, in contrast, are stakeholders in the company 
					(no partial ownership) with a risk profile that is different 
					from that of the shareholders. Lenders are focused on 
					immediate cash generation within the company whereas 
					shareholders, ideally, are more concerned with the long-term 
					growth of the company. This is because lenders advance funds 
					to a company on the understanding that they will be repaid 
					their principal in full at a pre-agreed time (if one bullet 
					repayment is to be made at the end of the loan) or on 
					regular intervals (if the loan is an amortising loan) 
					together with interest payments at regular intervals 
					throughout the life of the loan. Interest may be fixed or 
					floating but does not vary depending on the profits 
					generated by the company and reflects the level of risk that 
					a lender assumes by advancing the funds and is also driven 
					by international debt market forces. <br>
					</font><b><font face="Verdana" size="2" color="#000080"><br>
					Equity funding</font></b><font face="Verdana" size="1"><br>
					<br>
					A company may raise additional equity through a capital 
					increase. Increasing the capital requires the unanimous 
					approval of the shareholders of the company and entitles the 
					shareholders to subscribe for the new shares pro rata to 
					their existing participation in the company so as to avoid 
					any dilution of their participation. Any shares not 
					subscribed to by a shareholder of the company may only be 
					subscribed by the other shareholders of the company in 
					proportion to the shares owned by each of them. Only if all 
					shareholders agree, and subject to a no-objection and waiver 
					of pre-emption rights by all existing shareholders, can a 
					new shareholder invest in the company and subscribe to the 
					new shares, but the management cannot generally solicit the 
					public without violating the capital markets laws and 
					regulations. <br>
					Only public joint stock companies have access to the capital 
					markets and can raise capital through a public offering of 
					securities or a private placement of shares. A recent 
					example of an initial public offering is the Nawras IPO in 
					which Omani Qatari Telecommunications Company SAOG (under 
					transformation) issued 260,377,690 shares to the public. 
					Going public will submit a company to the rules and 
					regulations issued by the Capital Market Authority and the 
					Muscat Securities Market and will install a higher level of 
					transparency and disclosure on the company than is the case 
					for private or closed companies. <br>
					<br>
					</font><b><font face="Verdana" size="2" color="#000080">Debt 
					raising</font></b><font face="Verdana" size="1"><br>
					<br>
					By far the most common type of debt financing is a 
					straightforward loan secured against the assets of the 
					company. Other types of financing, such as overdrafts, 
					factoring, letters of credit, export credit finance, project 
					finance, etc. may also be available to certain types of 
					businesses.<br>
					A loan in its simple form is evidenced by an agreement 
					setting out the principal amount borrowed, the repayment 
					schedule, the interest and the interest payment dates. If 
					there is only one lender which is party to the loan 
					agreement, this is referred to as a bilateral loan. Where 
					multiple lenders provide financing to a company and are a 
					party to the same agreement a syndicate of lenders is formed 
					and the loan is referred to as a syndicated loan. 
					Syndication will be necessary where a company’s borrowing 
					needs exceed the borrowing capacity of any single lender 
					(bearing in mind that lenders look for a diversified 
					portfolio of loans and may therefore prefer to take a 
					smaller participation in a loan).</font></p>
					<p align="justify" style="margin: 3px 10px"> 
					<font face="Verdana" size="1"><br>
					A loan is a secured loan if the lenders have direct recourse 
					against the assets of the company in priority to the other 
					creditors of the company. To create priority in rank, the 
					security interest must be registered with the appropriate 
					governmental authority in Oman.<br>
					<br>
					Although most lenders prefer to be fully secured and to rank 
					first in priority, there is another class of investors, 
					called subordinated investors or mezzanine lenders. 
					Subordinated lenders may have the benefit of security but 
					agree to be paid in priority after any senior lenders have 
					been paid in full. The right to payment of principal and 
					interest of subordinated lenders will typically be subject 
					to payments under the senior loans having been properly made 
					and subordinated lenders will not be able to take any 
					enforcement action ahead of the senior lenders or without 
					the consent of the senior lenders. The contractual 
					arrangements between the various types of lenders (senior 
					and subordinated) are typically set out in an intercreditor 
					arrangement. The intercreditor agreement will contain what 
					is called a payment waterfall clause, contractually 
					cementing that payments of fees, costs, expenses, principal 
					and interest must first be made to the senior lenders. Once 
					the senior lenders have been paid in full, payments can be 
					made to the subordinated lenders, with any remaining 
					proceeds being payable to the most junior lenders and 
					ultimately to the borrower.<br>
					<br>
					Similarly to tapping the capital markets for equity share 
					capital through a public offering or private placement, 
					public joint stock companies can tap the debt capital 
					markets through the issue of bonds. A bond is a debt 
					instrument in the form of a security, entitling the holder 
					of the bond to repayment of principal plus interest. 
					Interest is either paid at the end (a so-called zero-coupon 
					bond) or is paid in regular installments (coupon).<br>
					<br>
					</font><b><font face="Verdana" size="2" color="#000080">
					Conclusion</font></b><font face="Verdana" size="1"><br>
					<br>
					The effect that an equity raising or a debt financing has on 
					a company is substantially different as shareholders will, 
					ideally, be focused on long-term equity growth and 
					generation of profits, while debt investors will be 
					concerned about the immediate generation of cash flow in 
					order to make payments of principal and interest at 
					pre-agreed times. However, both types of financing 
					structures will strengthen a company’s balance sheet and 
					make available the necessary funds to finance the company’s 
					operations. <br>
