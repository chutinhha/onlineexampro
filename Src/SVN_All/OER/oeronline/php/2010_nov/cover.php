					<font color="#000080" size="2" face="Verdana, Arial, Helvetica, sans-serif">
					<b>From darkness to light</b></font><font size="1" face="Verdana, Arial, Helvetica, sans-serif"><br>
					<br>
					<b>HE Mohammed Al Zubair, Adviser to His Majesty for Economic 
					Planning Affairs, likens Oman in 1970 to a dried tree which 
					sprouted again after the rain<br>
					</b>
					<br>
					Before 1970, Muscat (the area which now falls within Muscat 
					governorate) had two gates. One was located in the area 
					where Al Nahdah hospital now stands while the other one 
					separated Muscat from interiors of Oman. The second gate 
					which was used for imposing tariffs on the goods used to be 
					closed at night. But in the days just before the Renaissance 
					government vehicles and oil vehicles which had license were 
					allowed to go.<br>
					<br>
					Muscat (the area which now falls within Muscat city) was 
					surrounded by four gates: the big gate, the small gate, Al 
					Mathaib gate and Waljat gate. All these gates were closed by 
					9 pm, and 15 minutes before the closing of the gates drum 
					beats would be heard from Al Mirani Castle followed by 
					cannon firing announcing the closing of the gates. Those who 
					wished to enter after the closure of the city had to hold a 
					lantern (kerosene lamp) while they moved inside the city. If 
					the lamp was extinguished, its owner would be imprisoned 
					till the next morning and investigated. “Some of my friends 
					were troubled by this as our old house (now Bait Al Zubair 
					Museum) was located within the gates,” says HE Mohammed Al 
					Zubair, Adviser to His Majesty Sultan Qaboos bin Said for 
					Economic Planning Affairs.<br>
					<br>
					This heavy fortification was because a number of thefts 
					occurred in the area in the 40s and a foreign dignitary was 
					assassinated which alerted authorities that the number of 
					police personnel was not sufficient. But increasing the 
					number of police personnel was not possible at that time. 
					Therefore the system of gates, oil and kerosene lamps to see 
					the movement of individuals within the city came into force.<br>
					But the news of Renaissance was not a surprise to Al Zubair. 
					“Around six months before, we felt that a political change 
					was bound to happen. and at last the reality dawned on us on 
					July 23.”<br>
					<br>
					HE Al Zubair was among those who arranged the reception for 
					the new Sultan from Dhofar. They bought in bulk all the red 
					cloths – which was called Bandera – available in the market. 
					HE Al Zubair wrote the welcome boards with his hand and 
					helped many of his relatives to do so. They set up these 
					boards in all parts of Muscat and even in the palace. “When 
					the new leader and Sultan arrived at Bait Al Falaj Airport 
					House and delivered his historic speech it made the hearts 
					of the Omanis love him and elated over the Blessed 
					Renaissance.” In those times, Muscat was almost devoid of 
					major structures. The only major structure in Ruwi area was 
					the Bait Al Falaj airport which was situated in the midst of 
					farmlands. Even the spectacle of an aircraft coming from the 
					top of the mountains to land on the runway of the airport 
					was frightening to many people.</font><p align="left" style="margin:3px 10px; " dir="ltr"> 
