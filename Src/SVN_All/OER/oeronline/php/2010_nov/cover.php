					<font color="#000080" size="2" face="Verdana, Arial, Helvetica, sans-serif">
					<b>A proud nation</b></font><font size="1" face="Verdana, Arial, Helvetica, sans-serif"><br>
					<br>
					<b>Shura Council stands out as a proud symbol of the 
					parliamentary culture in the country, says HE Sheikh Ahmed 
					bin Mohammed Al Issai, Chairman of Shura Council</b><br>
					<br>
					The 40th anniversary of His Majesty Sultan Qaboos bin Said 
					coming to power in the country is a moment of national pride 
					in the mind of each and every Omani. It marks an auspicious 
					moment which will remain ineffable in the memory of Omani 
					people. The great achievements in this prosperous era 
					include the establishment of a modern and integrated state 
					with public institutions, laws, constitution and big 
					infrastructure facilities in all governorates and regions of 
					the country.<br>
					<br>
					These achievements are very conspicuous in all areas of 
					development especially in the progress of the life of Omani 
					people who are the real wealth of the nation. His Majesty 
					accorded top priority to the development of human resources 
					in the country through educating and training people and 
					equipping them with basic skills which will enable them to 
					work and to bring good for themselves, their society and the 
					country as a whole. <br>
					<br>
					The development of education is one of the significant 
					milestones in our development. These achievements include 
					providing quality health services, linking the whole country 
					with a good network of roads, quality water and electricity 
					services and many other things. <br>
					<br>
					Shura Council stands out as a symbol of civilisation in the 
					country. The functions of Shura began with the setting up of 
					a State Consultative Council in 1981. And after ten years, 
					exactly in 1991, the Shura Council came into existence.<br>
					<br>
					Since then, it continues to play a vital role in discussing 
					and studying various social issues, laws and bills referred 
					to it by the government and various other issues referred by 
					His Majesty. The Shura system was improved by setting up of 
					a State Council which also carries out significant national 
					responsibilities. Both State and Shura Councils together 
					constitute the Council of Oman. The Council of Oman’s 
					session begins every year with His Majesty’s speech focusing 
					on the actions to resolve various issues of the country. <br>
					<br>
					Since 1970 the country has accorded more importance to human 
					resource development and development plans. The forthcoming 
					Eighth Five Year Plan from 2011 to 2015 envisages massive 
					investment in industrial and tourism sectors which will be 
					greatly beneficial to Oman and its people. Basic and higher 
					education in the country will develop more with the increase 
					in the number of schools, colleges and private universities. 
					Education and health continue to be focal areas in the 
					government’s development priorities.<br>
					<br>
					One of the most prominent features of modern Oman is 
					security and stability that the Sultanate enjoys in a world 
					fraught with conflicts and problems. There is no doubt that 
					the insight of His Majesty, his political wisdom and realism 
					in dealing with foreign issues made Oman an oasis of peace 
					and stability and its citizens well-respected in all 
					countries of the world.<br>
					<br>
					The momentum of internal development coupled with 
					constructive foreign relations, establishment of the rule of 
					law and various public institutions, the development of 
					Shura Council, the importance accorded to the development of 
					Omani people and the economic policy focusing on 
					diversification of income sources are all positive 
					indicators that Oman will continue to march and fulfill its 
					ambition in all areas of life.<br>
					<br>
					Forty years is not a big time in the rise of any people. But 
					we have achieved a lot under the leadership of His Majesty 
					who created this modern state ­­ a nation that each Omani is 
					proud of and is widely appreciated all over the world.</font><p align="left" style="margin:3px 10px; " dir="ltr"> 
