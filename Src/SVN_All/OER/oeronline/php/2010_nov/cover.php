					existing Trade Law with a focus on economic development. It 
					was followed by various other related legislations including 
					the law for regulating the functions of Oman Chamber of 
					Commerce and Industry (OCCI), the law for regulating 
					functions of the banks, the Commercial Companies Laws, 
					electronic transactions law and Capital Market Law.<br>
