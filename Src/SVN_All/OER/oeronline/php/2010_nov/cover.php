					<font color="#000080" size="2" face="Verdana, Arial, Helvetica, sans-serif">
					<b>Post-Renaissance</b></font><font size="1" face="Verdana, Arial, Helvetica, sans-serif"><br>
					<br>
					“Oman after Renaissance is like a dried tree which received 
					rain and sprouted again after its death ,” says HE Al Zubair 
					describing the Sultanate after His Majesty took the reins of 
					government in the country. After 1970, everyone was drawn to 
					work in all fields and to perform different activities that 
					they were not able to do earlier in order to help the new 
					government. Everyone was willing to assist the government in 
					achieving development, especially since the country’s 
					population was very small and many people were immigrants. 
					Some of them returned immediately after hearing the news and 
					some of them came after waiting for a while. “Today our 
					country. is appreciated by everyone, as evident in the visit 
					of eminent personalities and senior dignitaries to the 
					country,” Al Zubair adds.<br>
					<br>
					</font>
					<font color="#000080" size="2" face="Verdana, Arial, Helvetica, sans-serif">
					<b>Harmonious development</b></font><font size="1" face="Verdana, Arial, Helvetica, sans-serif"><br>
					<br>
					<b>In 1970, His Eminence Sheikh Ahmad bin Hamad Al Khalili, 
					Grand Mufti of the Sultanate felt that the country was on 
					the threshold of a new era</b><br>
					<br>
					The achievements the Sultanate made under the leadership of 
					His Majesty Sultan Qaboos bin Said are plenty. But one of 
					the most important achievements has been the development of 
					Omani human resources. Omani citizens have become able to 
					perform in various areas of life and played a remarkable 
					role in the Renaissance. “The development of human resources 
					is one of the most important of all achievements, because it 
					is different from building structures, says His Eminence 
					Sheikh Ahmad bin Hamad Al-Khalili, Grand Mufti of the 
					Sultanate. Buildings can be made in a short period, but 
					saving man from underdevelopment and making him advanced and 
					capable of understanding his time and its issues are not 
					that easy.<br>
					<br>
					“Omani citizens are obliged to thank God for these blessings 
					and use them for their spiritual and ethical uplift,” says 
					Khalili. He reiterates that we need to synthesise spiritual 
					renaissance with material renaissance. For, material 
					renaissance devoid of spirituality is of no value. “So this 
					Renaissance demands us to thank God if we really want to 
					maintain it. We also need to take utmost care to preserve 
					our faith and spiritual values and strengthen our 
					Renaissance with good deeds and lofty morals.”<br>
					<br>
					Referring to the main reasons that contributed to these 
					achievements, His Eminence Al Khalili says, “Sincerity of 
					leadership and the harmony between the top and bottom of 
					society are the main reasons for these achievements.” The 
					people of Oman were awakened by the Renaissance to 
					understand their worth and potentials.<br>
					<br>
					Recalling the moments when His Majesty came to power in 
					1970, he says, “I felt that we were on the threshold of a 
					new era and the change would liberate people from narrowness 
					to broadness, from rigidity to openness and from constrains 
					to relaxation. We received the news with joy, because people 
					were elated with this and they became optimistic that today 
					will be better than yesterday and tomorrow will be better 
					than today.<br>
					Referring to the future of the country, he says, “The future 
					of the country in the prosperous era of His Majesty will be 
					a bright one. If the country could achieve such a great 
					success starting from zero, there is no doubt that the 
					future built on these achievements will be far bigger. With 
					sincerity and harmony between the top and the bottom we 
					could achieve bigger achievements.”<br>
					<br>
					</font>
					<font color="#000080" size="2" face="Verdana, Arial, Helvetica, sans-serif">
					<b>Bright prospects</b></font><font size="1" face="Verdana, Arial, Helvetica, sans-serif"><br>
					<br>
					<b>HE Sheikh Salim al Khalili, Minister of Agriculture, 
					believes that the country holds out a prosperous and 
					promising future for its coming generations</b><br>
					<br>
					We were in Baushar when a messenger came to my father with a 
					letter from Sheikh Ahmed bin Ibrahim Al Harthy who was the 
					administrator of Al Dakhliyah. The letter brought in the 
					happy news about the upcoming changes in the Sultanate. It 
					also quoted the famous speech by His Majesty Sultan Qaboos 
					bin Said asking Omanis to forget about the past and inviting 
					them to stand for unity.<br>
					<br>
					It is easy for a person to speak of any event that happens 
					in the world today, but it is not that easy to talk about 
					the Renaissance which provided us with welfare in this world 
					and happiness hereafter. It has brought about all 
					achievements, welfare and goodness that now we are living 
					with. There are men in history who came to build their 
					nations and people and I believe that His Majesty, with all 
					the achievements he has brought to the country, is one of 
					those rare historical figures.<br>
					<br>
					</font>
					<font color="#000080" size="2" face="Verdana, Arial, Helvetica, sans-serif">
					<b>Comprehensive growth</b></font><font size="1" face="Verdana, Arial, Helvetica, sans-serif"><br>
					<br>
					His Majesty accorded a lot of importance to the development 
					of human resources. He understood the significance of 
					education and knowledge in this regard as he famously said, 
					“We will know our children, even under the shade of the 
					trees.” He set up schools, institutes, college and 
					universities for this society which did not have access to 
					the world of education prior to 1970. There were only three 
					schools in the country until that time – one each in Muscat, 
					Muttrah and Salalah. His Majesty surpassed all boundaries 
					and rose up to the expectations and aspirations of the 
					people His Majesty also gave priority to providing health 
					services and set up a number of health service institutions. 
					Today the Sultanate is one of the few countries that managed 
					to provide high quality health services. Besides, he 
					provided people with other services such as electricity, 
					water, roads, airports and ports, and promoted agriculture. 
					He was also awakened to the importance of food security and 
					supported people working in agriculture and fisheries and 
					various other trades in order to make them self-sufficient 
					in their respective jobs Another major achievement was the 
					system of safety and security as understood by anyone who 
					comes to the country. Everybody can see symbols of this 
					development anywhere in the country. When one goes up Jabal 
					Shams, road facilities, electricity and other services there 
					are indicators to the fact that the Blessed Renaissance has 
					reached there also.<br>
					<br>
					</font>
					<font color="#000080" size="2" face="Verdana, Arial, Helvetica, sans-serif">
					<b>A model for development</b></font><font size="1" face="Verdana, Arial, Helvetica, sans-serif"><br>
					<br>
					<b>The achievements brought about by His Majesty are really 
					indescribable. All these things in this country constitute a 
					model for progress in security, health, education, 
					transportation, roads and electricity in the country. </b> <br>
					<br>
					His Majesty has created a lot of mosques, minarets and 
					religious centres. There is not a single Wilayat in the 
					country without a mosque. One of the greatest and most 
					prominent of these is the Sultan Qaboos Grand Mosque. I 
					speak like this because I lived both before and during the 
					time of His Majesty. I wish I had not lived except for in 
					the time of His Majesty.<br>
					<br>
					The setting up of Shura and State Councils is one of the 
					hallmarks of his time. His Majesty intends to make use of 
					the potential of the Omani people; therefore he appoints 
					aged people who have retired to State Council in order to 
					utilise their skills for the service of nation and its 
					people and contribute to the Renaissance of the country.<br>
					<br>
					I believe His Majesty could achieve all these things because 
					God has bestowed wisdom on him. It was this wisdom given by 
					the Almighty that served as a secret to fulfill all these 
					achievements. Besides, the commitment of Omani people and 
					their gratitude to God dovetailed nicely with these 
					distinctive divine blessings.<br>
					<br>
					His Majesty achieved in the last forty years what Oman could 
					not achieve in its whole past both in terms of the power of 
					the state and its status and prestige outside. Oman is one 
					of the rare countries in the world which does not have 
					enmity with any other country in the world. Omanis are 
					respected and held in high esteem everywhere.<br>
					<br>
					Owing to the great achievements the country has made, I 
					believe that the country holds out a prosperous and 
					promising future for its coming generations. His Majesty has 
					laid solid foundations for future generations promising them 
					happier life. Moreover, I am confident that Omani people are 
					hard working and will fulfill their ambitions in any way. I 
					am also confident that the future will be bright for us. God 
					will not delay rewarding such great work.<br>
					<br>
					</font>
					<font color="#000080" size="2" face="Verdana, Arial, Helvetica, sans-serif">
					<b>A success story</b></font><font size="1" face="Verdana, Arial, Helvetica, sans-serif"><br>
					<br>
					<b>The private sector has contributed to the development of the 
					country through its partnership with the public sector, says 
					HE Khalil bin Abdullah Al Khonji, Chairman, Oman Chamber of 
					Commerce and Industry</b><br>
					<br>
					During the last 40 years the Sultanate’s economy has made a 
					great leap forward driven by the mechanism of free market, 
					keeping abreast of the innovations of modern age and giving 
					priority to the growth of all economic sectors such as 
					industry, manufacturing tourism, services, information 
					technology etc. Under the wise leadership of His Majesty 
					Sultan Qaboos bin Said the country entered into trade, 
					economic and investment agreement with various economic 
					groups and organisations as well as neighboring countries.
					<br>
					<br>
					HE Khalil Bin Abdullah Al Khonji, Chairman of Oman Chamber 
					of Commerce and Industry (OCCI), attributes the 
					post-Renaissance transformation of Oman to a modern economic 
					powerhouse to inclusive strategies and broad vision with 
					focus on balanced and comprehensive development. “The Omani 
					model of development stands out with its focus on gradual 
					and balanced growth rather than immediate results. This 
					enabled the country to achieve a solid and firm economic 
					base which helped Oman to stand tall through the global 
					economic crisis. Oman was listed among the least affected 
					countries during the financial meltdown,” says HE Khonji.<br>
					<br>
					According to HE Khonji, the hallmark of Oman’s economic 
					growth across all sectors has been stability and balanced 
					fiscal policies. It stimulated commercial and industrial 
					activities in the country and helped attract foreign 
					investments to the country especially in the newly 
					developing zones in Sohar, Duqum, Salalah and other regions.<br>
					<br>
					</font>
					<font color="#000080" size="2" face="Verdana, Arial, Helvetica, sans-serif">
					<b>Vision 2020</b></font><font size="1" face="Verdana, Arial, Helvetica, sans-serif"><br>
					<br>
					HE Khonji observes that Oman’s Vision 2020 was able to take 
					tangible steps to help Oman’s transformation from a 
					traditional economy to a modern one by developing the entire 
					manufacturing and service sectors and increasing their 
					contributions in the GDP through diversifying the national 
					economy. Owing to these measures, the contributions of 
					industry, tourism and service sectors to the GDP have 
					considerably increased. These sectors continue to play an 
					active role in the development of national economy by 
					attracting local and foreign investments and training and 
					employing Omani youths. They have also introduced modern 
					technology and know-how in different areas.<br>
					The year 2010 marks the end of the seventh five-year plan, 
					the third plan within Vision 2020, and the beginning of the 
					Eighth Five-year plan, the fourth within the vision, with 
					several new comprehensive development plans. HE Khonji 
					observes that at this crucial juncture the private sector, 
					as a development partner of the government, is looking 
					forward to playing a bigger and more effective role with 
					special focus on investment in service sectors.<br>
					<br>
