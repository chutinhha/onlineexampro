					<b><font face="Verdana" size="4" color="#C00000">
					Oman 
					at 40</font><font face="Verdana" size="2" color="#CC3333"><br>
					<br>
					</font></b>
					<font size="1" face="Verdana, Arial, Helvetica, sans-serif">
					&nbsp;A report by <b>Khalfan al Rahbi</b>, <b>Fatma al 
					Araimi</b>, <b>Visvas Paul D Karra</b> and <b>Muhammed Nafie</b><br>
					<br>
					<br>
					It was 40 years ago that Oman made its historic tryst with 
					the Renaissance, ushering in a new dawn of progress and 
					prosperity in the country. In his landmark oration capturing 
					the essence of this great transition moment, His Majesty 
					Sultan Qaboos bin Said had promised to transform the country 
					‘into a prosperous one with a bright future’ and called on 
					people to work in unity and cooperation for designing the 
					future course of the nation.<br>
					<br>
					The visionary leader has proved as good as his words and the 
					people have accepted the challenge of the future with a 
					remarkable sense of responsibility and commitment. Over the 
					last forty years Oman has been rediscovering itself. A 
					farsighted leader and his dedicated people have created the 
					country’s history anew. While the country commemorates the 
					40th anniversary of its National Day, it is a moment to 
					celebrate not only the triumphs and achievements but also 
					the grandeur of the striving and efforts behind it and the 
					vision and ideals which moulded it. As Albert Einstein 
					famously said all that is valuable in human society depends 
					upon the opportunity for development accorded to the 
					individual. Oman has made remarkable strides towards the 
					development of its human resources which, in turn, has 
					significantly contributed to its social development and 
					economic growth.<br>
					<br>
					It is also the moment to celebrate our national integration 
					which helped us achieve what a disoriented and under- 
					resourced country could never dream of in 1970. The joining 
					together of all people-including those who had left for 
					greener pastures and the erstwhile disgruntled exiles- under 
					one umbrella to get the nation a respected position in the 
					comity of nations was an extraordinarily inspiring event in 
					the history of any nation. In the following pages, some 
					eminent personalities and business heads walk down memory 
					lane recollecting the phenomenal transformation of the 
					country over the last four decades.
					</font>
