		inflation.<br>
		<br>
		Inflation jumped to a record of 9.5 per cent in January driven mainly by 
		a 16.1 per cent rise in housing costs. The value of all real estate 
		deals-including residential, investment and commercial property-rose to 
		around KD277.4 million (US$1.04 billion) in March compared to KD240.5 
		million dinars a year ago, government data obtained by Reuters showed.<br>
		<br>
		Residential sales, the biggest part of the real estate business, rose to 
		KD143.46 million in March compared to KD140.53 million in the same month 
		last year. Commercial property jumped 165 per cent to KD35.34 million, 
		while investment property rose around 13.74 per cent to KD98.56 million. 
		The total number of deals fell to 873 in March from 1,065 a year 
		earlier, the data showed. <br>
		<br>
		The central bank urged the government last week to tackle record 
		inflation by giving away more land to citizens. The government said in 
		February that it raised subsidies on building materials such as steel 
		and cement. More than 90 per cent of land is owned by the government.<br>
