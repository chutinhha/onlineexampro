		<b>Saudi ‘not yet ready to open up’</b><br>
		Saudi Arabia is not quite ready to fully open up to visitors despite 
		welcoming its first cruise passengers to Jeddah last month, according to 
		a tourism official. “Realistically, we are just at the start of the 
		creation of the service side of tourism, as well as infrastructure, and 
		we want to be in good shape first,” Saudi Commission for Tourism and 
		Antiquities (SCTA) Secretary General, Prince Sultan bin Salman bin 
		Abdulaziz Al-Saud said.<br>
		<br>
		The commission will place renewed emphasis on promoting the country’s 
		domestic tourism sector, and its foremost challenge will be to attract 
		the local audience, five million of whom vacation overseas each year. 
		Domestic tourism is predicted to soar to 73.3 billion riyals (US$19.5 
		billion) in 2010 and 101.3 billion riyals in 2020, according to a recent 
		report by the Saudi Arabian Monetary Agency (SAMA). There are also 
		measures in the offing to streamline annual vacations that give people 
		shorter holidays. The commission also plans to provide seed capital to 
		event organisers to create MICE attractions in the kingdom in a bid to 
		drive demand. Other recent developments include the classification of 
		hotels, which has already begun in Mecca and Medina, with a major study 
		underway to research the possible expansion of a heritage inn chain 
		similar to the ‘parador’ system in Spain.<br>
