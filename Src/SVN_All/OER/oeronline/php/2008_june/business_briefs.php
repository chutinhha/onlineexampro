<font size="2" color="#CC3300" face="Verdana"><b>Towell Auto Centre launches 
TOTAL OIL Change centre</b></font><font face="Verdana" size="1"><img border="0" src="towell_auto_oil.jpg" width="138" height="112" align="right" hspace="6" vspace="8"><br>
Towell Auto Centre has launched TOTAL Oil change centre at Al Ghubrah to offer 
quick oil change along with their existing services of auto/ manual car wash, 
polishing and service registration. The high quality range of TOTAL engine oils 
is now available at this centre and includes the TOTAL Quartz range of SL grade 
