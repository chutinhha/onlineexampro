<br>
</font><font size="2" color="#CC3300" face="Verdana"><b>DME Brent, Oman crude 
contracts start</b></font><font face="Verdana" size="1"><br>
The Dubai Mercantile Exchange Limited (DME) has commenced trading of the two new 
financially settled futures contracts for Brent and Oman crude oil. The DME 
Brent Crude Oil Financial Contract will be cash-settled against ICE’s Brent 
Crude Futures Contracts, while the DME Oman Crude Oil Financial Contract will be 
cash-settled against the DME Oman Crude Oil Futures Contract. The DME, a joint 
venture between the New York Mercantile Exchange, Inc (NYMEX), Tatweer, a member 
of Dubai Holding, and the Oman Investment Fund (OIF), provides a financially 
secure, well-regulated and transparent trading environment. It also addresses 
the growing market need for price discovery of Middle East Sour Crude Oil.</font><hr width="90%" size="1">
