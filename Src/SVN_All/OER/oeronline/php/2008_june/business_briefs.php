<font size="2" color="#CC3300" face="Verdana"><b>Renaissance acquires Doha 
Marine </b></font><font face="Verdana" size="1"><br>
Renaissance Services SAOG subsidiary Topaz Energy and Marine Ltd (Topaz) made a 
dramatic entry into the buoyant hydrocarbon market in Qatar with the acquisition 
of the Doha Marine Services (DMS) fleet of 14 owned and managed vessels. The 
entire transaction is valued at QR453 million (approx RO49 million). The 
acquisition has increased the total size of the Topaz’s marine fleet to 91 
vessels. Maqbool bin Ali Sultan, Minister of Commerce and Industry described the 
venture as an “important event in the private sector commercial relations of 
Qatar and Oman”. According to Renaissance Chairman Samir J Fancy, “The DMS 
acquisition is a major event in our stated strategic intent to increase the size 
and geographical spread of our offshore support vessel fleet.”</font><hr width="90%" size="1">
