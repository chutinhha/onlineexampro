<font size="2" color="#CC3300" face="Verdana"><b>CMA nod for re-insurance 
company</b></font><font face="Verdana" size="1"><br>
The Capital Market Authority (CMA) sanctioned the establishment of the first 
Oman Re-insurance Company with a RO30 million capital base. Oman Chamber of 
Commerce and Industry (OCCI), as well as, private and public companies operating 
in the fields, such as, banks, insurance, services and investment will 
contribute to the establishment of the new company. The new entity will be 
listed on Muscat Securities Market five years from commencing operations. <br>
Omantel pumps US$50 m into submarine cable project<br>
<br>
Oman Telecommunications Company (Omantel) will invest US$50 million in the 
world’s first direct submarine cable system – the Europe India Gateway cable. 
The company and 16 other global telecommunication firms entered into a 
maintenance agreement to construct the high bandwidth optical fibre cable from 
the United Kingdom to India, passing through the Sultanate and other countries.<br>
<br>
The US$700-million project will significantly enhance capacity and diversity 
between countries in these regions to serve the rapidly growing 
telecommunications sector and investment environment said Dr. Mohammed bin Ali 
al Wohaibi, Chief Executive Officer of Omantel. He added that Omantel plans to 
invest about US$100 million in submarine cable projects with the objective to 
increase international cable capacities and provide diversity. The new 15,000km 
submarine cable system will connect 13 countries, including the Sultanate and 
three continents. The EIG cable system is expected to carry commercial traffic 
in the second quarter of 2010.</font><hr width="90%" size="1">
