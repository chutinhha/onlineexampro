<font size="2" color="#CC3300" face="Verdana"><b>PDO set to double condensate 
production </b></font><font face="Verdana" size="1"><br>
Petroleum Development Oman (PDO) aims to double condensate production during 
2008, following the commissioning of the Kauther Gas Plant last November, two 
months ahead of schedule. “Last year, our gas production was equivalent to 
395,000 barrels of oil per day, while condensate production grew to an average 
of 46,000 barrels per day,” Managing Director, John Malcolm stated in the 
company’s 2007 Annual Report. Natural gas supplied by PDO to the Government Gas 
System (GGS) averaged 16.4 million cubic metres per day, which was higher by 0.7 
million cubic metres per day compared to volumes supplied in 2006. The Kauther 
Gas Plant’s share was around 0.6 million cubic metres per day, the report said.</font><hr width="90%" size="1">
