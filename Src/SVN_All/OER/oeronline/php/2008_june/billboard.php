		decided to expand the facility to extend its reach and range of services 
		and raise quality of service standards throughout the company.<br>
		<br>
		The call centre is equipped to provide prompt information about flight 
		schedules, fares, reservations, ticketing, holiday packages, Oman Air’s 
		Sindbad FFP and other airline information. Customers can now even buy 
		their tickets over the phone using their credit cards. Currently the 
		call centre covers the entire Sultanate and handles about 55,000 calls a 
		month. The ultimate aim is to extend the call centre’s service to the 
		entire AGCC region and the Middle East by the end of this year. Oman Air 
		has registered a substantial increase of 50 per cent in passenger 
		traffic for the first quarter of this year. According to Abdulrazaq Bin 
		Juma Alraisi, Senior Manger, Sales, the airline has also posted a 
		considerable growth of 103 per cent in sales. Also, the number flights 
		this quarter have been increased to 1,860 compared to 1,373 flights for 
		the same period last year. The airline added Alraisi, is geared up for 
		further growth with the addition of the new Boeing Dreamliner aircraft 
		and plans to increase market share. Though the airline posted losses for 
