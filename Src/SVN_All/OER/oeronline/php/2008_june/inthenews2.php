		<td width="322" valign="top"><font face="Verdana" size="1">The e-transactions law of Oman legalises the use of digital signatures in 
electronic commerce and communications through letters, e-mails, etc. 
Organisations wishing to introduce digital signatures for their use must first 
obtain approval from the Information Technology Authority through a formal 
process, he added.<br>
		<br>
In order to assure adequate protection to both businesses and the common man, 
this law provides for penalties in the event of electronic crimes involving 
e-transactions. Illegal use, disclosure and break-ins into computer systems, 
unauthorised tampering of encryption systems are also punishable crimes under 
this law, Khusaibi said.<br>
		<br>
The law gives credibility to electronic information sharing, while taking the 
issue of information and communication security to higher levels of 
authentication, integrity and privacy. It provides for encryption as a means to 
protect e-transactions in order to ensure confidentiality of information or data 
of the message, to verify the originator, and to prevent others from getting 
information or data messages so as not to reach the addressee or to corrupt 
them. Government entities can now use electronic records and digital signatures 
in tendering systems. Licence request processing and taxation systems can also 
accept fees or any payments electronically.<br>
<br>
Protection for adequate privacy in matters related to personal data held in 
computer systems and processes for enabling electronic transactions are ensured 
by the new e-transactions law of Oman. The law leaves the responsibility of 
protecting electronic systems and data on the entities that manage these 
electronic systems and the authorised entity that issues valid digital 
signatures. </font></td>
