Royal Decree No. 69/2008 promulgates the Electronic Transactions Law.<br>
		<br>
Article I enforces the attached Electronic Transactions Law.<br>
<br>
Article II says the Minister of National Economy shall issue the necessary 
regulations and decisions to implement the provisions of this law.<br>
		<br>
Article III cancels all that contradicts this law or contravenes with its 
provisions.<br>
Article IV says the decree takes effect the day following its date of 
