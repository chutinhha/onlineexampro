</font><b><font face="Verdana" size="4">Sultanate’s first e-transactions law</font></b><font face="Verdana" size="1"><br>
<b>Oman’s first e-transactions law gives credibility to electronic information 
sharing, while ensuring higher levels of security, authentication, integrity and 
privacy </b><br>
<br>
The promulgation of the Electronic Transactions Law by His Majesty’s Royal 
Decree No. 69/2008 begins a new era in Oman, where a truly e-enabled society 
evolves in the realisation of the digital society of the Sultanate, according to 
HE Mohammed Bin Nasser Al Khusaibi, Secretary-General of the National Economy 
Ministry and Chairman of the Information Technology Authority (ITA). It is a 
major milestone in the implementation of the national IT strategy by the 
Information Technology Authority of Oman.<br>
<br>
This is the first law for legalising electronic transactions in Oman which can 
be defined as any contract, agreement or communication to be fully or partially 
implemented by electronic means as electronic messages, he explained.<br>
<br>
The ITA, along with other government entities and a private law firm, has 
compiled this law based on studies by the United Nations Commission on 
International Trade Law (UNCITRAL), Organisation for Economic Cooperation and 
Development (OECD) guidelines on the protection of privacy and trans-border 
flows of personal data, and e-laws of several countries like the US, Europe, 
France, Ireland, Malaysia, Tunisia, etc.<br>
<br>
The e-transactions law of Oman legalises the use of digital signatures in 
electronic commerce and communications through letters, e-mails, etc. 
Organisations wishing to introduce digital signatures for their use must first 
obtain approval from the Information Technology Authority through a formal 
process, he added.</font></p>
