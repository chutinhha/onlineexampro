<b><font face="Verdana" size="4">Charge up that car battery with these tips</font></b><font face="Verdana" size="1"><br>
<b>Batteries too suffer damage during the harsh summer months. You can improve the 
life of your car battery and its functionality with a few tips</b><br>
<br>
<img border="0" src="spotlight4.jpg" align="right" vspace="5" width="260" height="184" hspace="5">The good performance of any automobiles depends on its battery. Made of plastic, 
lead and electrolyte, batteries start to activate the engine of the vehicle 
through a short blast of high power. Unfortunately, the automotive battery is 
one of many highly abused, seldom maintained, auto part even though it is a very 
important component. Usually no attention is paid to the battery unless it is 
suspected of causing a problem. The most common problem encountered with 
batteries is low voltage or low charge. <br>
<br>
Depending on external conditions and the length of time for which they are 
running, auto batteries may for about two to four years under the best 
conditions. The length of service of the battery can even be shortened to year 
or less, when exposed in very hot temperature and environment. Other factors too 
make them “sulfated” ahead of time and affect their performance. These include 
continuous charging problems and constant short-period driving. Very hot climate 
and overcharging can dry off the battery, making the water inside lower than the 
top of the cell plates and not in the ideal level.<br>
<br>
Just like other auto parts, batteries need proper maintenance. For unsealed 
types of battery, adding distilled water must be done once in a while so as to 
maintain the proper water levels. This is especially required during summer 
period since water evaporates more rapidly. It is important to not let batteries 
run out of water in order to avoid battery cell problem and further damages. 
Unsealed batteries can be maintained and recognized without difficulty because 
of the small vent caps above the battery that can simply be twisted open. On the 
other hand, the sealed battery does not require adding water to its cells unlike 
in the unsealed type of battery.<br>
<br>
Whether your car uses a sealed or unsealed battery, certain measures must be 
done in order to lengthen its service and keep it functioning properly. The 
following are the significant ways on maintaining the battery of your vehicle.<br>
<br>
<b>Clean the cables clean </b><br>
Before the cleaning process, make sure that the cables are disconnected from the 
auto battery. After that, the cables can be cleaned using a wire brush which has 
been soaked in a home-made mixture. Same solution is employed when cleaning the 
top portion of the battery. The mixture is a combination of one cup of water and 
one tablespoon of baking soda.<br>
<br>
<b>Apply lubricants on the posts </b><br>
Aside from keeping the cables clean, applying petroleum jelly on every post can 
aid in preventing rapid corrosion just like in auto radiators. The lubricant 
likewise makes an easy sliding of the cables when attaching them back on the 
battery.<br>
<br>
<b>Ensure connections </b><br>
There are cars that does not use or require a battery hold down bar. However, if 
the vehicle has one, it is important to keep it in a secure position.<br>
<br>
<b>Battery Maintenance: Essential Upkeep</b><br>
Replacing a car battery can be a hassle — especially when a current battery 
dies, leaving you stranded and in dire need of a jump-start. It’s for this 
reason, that we want you to know how to keep your car battery in top-notch 
condition. Take note of these Do’s and Don’ts to help your auto battery maintain 
its peak performance and avoid a low charged battery: <br>
<br>
<font color="#FF9933"><b>Do’s </b></font> <br>
<ul>
	<li>
	<p align="justify">Whenever working around batteries, make sure to wear proper eye, hand, and 
clothing protection.Check the terminals where the battery cables connect to be sure they are tight 
and free of corrosion. If corrosion is present, get a qualified technician to 
clean the terminals. <li>
	<p align="justify">Make sure the battery is firmly secured to its mounting bracket. An unsecured 
battery that shifts around can become damaged, and possibly cause short 
circuits. </li>
	<li>
	<p align="justify">In batteries other than those that are “maintenance free,” periodically check 
the fluid level. If the fluid is low, add only distilled water to top it off. If 
no fluid is detectable, you may want to replace the battery rather than fill it, 
as batteries in this condition will usually fail very soon. </li>
	<li>
	<p align="justify">Always remember to keep your battery case clean. Dirt conducts electricity, 
which can discharge the battery. Battery cases can be cleaned with a solution of 
baking soda dissolved in warm water. Wet the case and agitate with a nylon 
bristle brush. Rinse well with plain water. </li>
	<li>
	<p align="justify">Batteries come in many different sizes. When replacing a car battery, make sure 
you choose the right size for your car. When it comes to car batteries, bigger 
is not always better. <br><br><b><font color="#FF9933">Don’ts </font></b><br><li>
	<p align="justify"></li>If you suspect that a battery is frozen, do not charge it, as it may explode! 
One visual sign that a battery has frozen is that the sides are bowed out. This 
condition is not repairable, and the battery will need to be replaced by a 
professional as soon as possible. </li>
	<li>
	<p align="justify">If you need to charge your battery yourself, switch the charger to a low-charge 
setting. Most chargers have this feature but if not, have a professional charge 
the battery. </li>
	<li>
	<p align="justify">Don’t charge a dead battery with a car’s alternator. An alternator is not 
designed to function as a charger, and it may be damaged or have a shortened 
life as a result. <br>Use user’s manual or professional’s help for proper dispose of dead batteries 
for environmental aspects.</li>
	<li>
	<p align="justify">Maintaining the auto battery is a very simple task and must be done at least 
once in 3 months. A properly working battery not just makes any travel 
hassle-free &amp; safe at driving.<br><i><br>These maintenance tips and advice were contributed by Ravi K Setty, Sr Manager - 
TBA Division OTE Group – Sole Distributors for Exide Batteries</i><br><br><b>A few more battery maintenance tips...</b><br>If you are one of those who thinks ‘maintenance free’ car batteries prevent the 
headache and hassles of car battery maintenance, it’s time to do a rethink. 
Given the number of times you’ve experienced battery failure, it would be far 
more prudent to chose a low maintenance car battery and follow strict 
maintenance norms. Following the right car battery maintenance tips not only 
extends the life of your battery, it also ensures smooth, uninterrupted rides 
for you.<br><br><b>Automotive Batteries</b><br><li>
	<p align="justify"></li>Test your battery on a regular basis, including when you have your car 
serviced, before long trips or after it’s been recharged.</li>
	<li>
	<p align="justify">Maintain all areas/parts of your vehicle as directed in your owner’s manual 
and/or by your mechanic. This will reduce the chance of other engine components 
draining power from your battery. It’s not always the battery’s fault when a car 
doesn’t start, but often a symptom of other problems.<br><br><b>Maintenance tips for extended battery life</b><br>
	<li>
	<p align="justify"></li>Regularly clean the battery container and terminals</li>
	<li>
	<p align="justify">Make sure vent caps are tight to prevent water or baking soda from entering 
cells</li>
	<li>
	<p align="justify">Apply baking soda to any corrosion and rinse cover with water</li>
	<li>
	<p align="justify">Check your battery’s electrolyte level before charging, it should be at the 
bottom of the vent splash band:</li>
	<li>
	<p align="justify">Electrolyte level low? Add distilled water to bring level to the bottom of the 
vent walls.</li>
	<li>
	<p align="justify">Distilled water not available? Use a good drinking water. It's better than 
adding nothing; otherwise the battery plate will dry out and the battery could 
