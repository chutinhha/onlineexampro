                <td height="18"> <p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"> 
                    <br>
                    </font><b><font face="Verdana" size="4">The White Man’s 
                Burden</font></b><font face="Verdana" size="1"><br>
                <b>The White Man’s Burden is a perfect account of the problems 
                faced because of financial aid from the West. William Easterly’s 
                book combines compassion and outlines solutions. Sunil Fernandes 
                reviews</b><br>
                <br>
                <img border="0" src="bc.jpg" align="left" hspace="8" vspace="6" width="150" height="250">The poor around the globe are suffering from myriad problems. 
                After decades of financial assistance from the West, that run 
                into trillions of dollars, children are still dying from 
                preventable diseases, people are still starving and there’s lack 
                of access to drinking water, sanitation and basic education. The 
                problems are manifold. <br>
                <br>
                William Easterly, Professor of Economics at New York University 
                with a long track record in development, tackles the causes of 
                the problems and the solutions in his book. The results make for 
                uncomfortable but compelling reading. The book cites distressing 
                examples, including Schistosomiasis cases in Malawi; sewage 
                problems in Brazilian favelas and malaria epidemic in Ethiopia.
                <br>
                <br>
                The book is an indictment of the failure of western aid to reach 
                the poor. William Easterly comes down heavily on bureaucracy, 
                which according to him is one of the culprits for the present 
                problems. He cites an example of the roads in Tanzania, where 
                foreign aid donors spent $2bn during the past 20 years building 
                roads. The roads deteriorated faster than donors built new ones, 
                due to lack of maintenance. “The poor need roads; the aid 
                bureaucracy fails to deliver them,” Easterly says in his book. 
                The author dwells on the pathetic conditions in Adis Ababa, the 
                AIDS condition in Africa and the sanitation and health related 
                problems there. <br>
                <br>
                Easterly says that getting back to basics would help. According 
                to him, aid agents should be held accountable for individual, 
                feasible areas for action, that help poor people lift themselves 
                up. He also says that it is important to evaluate, based on 
                feedback from the intended beneficiaries of scientific testing.
                <br>
                <br>
                The other way going forward according to the author is to reward 
                success and penalise failure. Among his solutions also include 
                getting more money for interventions that are not working. “Each 
                aid agent should explore and specialise further in the direction 
                of what they are good at doing,” the book notes. <br>
                <br>
                Easterly says that one should not abandon all hope. He recalls 
                that even with all the possible bureaucratic pitfalls to making 
                aid work, it sometimes does work. The author points to a 
                successful project in the World Bank’s portfolio for example. He 
                recounts how the World Bank in the early 1990s helped fund the 
                Food for Education programe to keep Bangladeshi girls in school 
                by giving their parents cash payments in return for school 
                attendance. The result was that enrollment doubled in the areas 
                covered by the programme. The book also focuses on success 
                stories like Japan, India, China, the East Asian Tigers, Turkey, 
                Botswana and Chile. “Americans and Western Europeans will 
                someday realise that they are not the saviours of the rest,” 
                Easterly argues. All these countries have managed with 
                self-help. <br>
                <br>
                All in all, the book makes for compelling reading.</font></p></td>
