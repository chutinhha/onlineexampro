            <font size="4" face="Verdana, Arial, Helvetica, sans-serif">Oman an 
            Attractive Market: Experts<br>
            </font>
                </b><font size="1" face="Verdana, Arial, Helvetica, sans-serif"><br>
            </font><font face="Verdana" size="1">
            <img border="0" src="news.jpg" align="left" hspace="8" vspace="6" width="295" height="193">The global economic crisis
has had less of an impact
on Oman and it continues
to remain an attractive
market, according to
some real estate experts
who spoke at the recently concluded
seminar on ‘Global crisis and its impact
on the real estate sector,’ organised by
Iskan Oman Investment Company and
supported by Oman Chamber of Commerce
and Industry.
<br>
            <br>
            “Three years ago, we commissioned
a feasibility study for the entire GCC
region. The objective of the study was
to analyse the GCC market for the
regional expansion of Iskan Kuwait.
The study was conducted by an independent
third party, which is a leading
consultancy firm with high international
credibility. Oman was selected
to be the first country for regional expansion.
The decision was guided by
stability of the country and the sound
regulations that govern foreign investment.
We are here to participate in
the development of the country and
to share our experiences,” Adil Mohammed
Al Bader, Chairman of Iskan
Oman Investment Company, stated.
<br>
            <br>
            Dr Rashid Ali Al Balushi, Managing
Director of Iskan Oman, said that
Oman was less impacted by the global
economic crisis and the fact was borne
out by a recent survey of Merrill Lynch
that includes Oman among the 10 least
vulnerable economies in the world.
Prof Manoj Pant, Professor, Centre for
International Trade &amp; Development,
JNU, India attributed a prime reason
for the current global crisis on the ‘lack
of demand’.
<br>
            <br>
            According to him, fiscal measures
would be more important than monetary
measures. “Governments across
the globe need to propel demand by increasing
investments substantially, be
it infrastructure, healthcare or a host of other areas, that can lead to 
demand,”
he said.
<br>
            <br>
            Paul Arnold, Director, Business Advisory
Solutions – Real Estate, Hospitality
&amp; Leisure, Ernst &amp; Young, said that
the firm remained bullish in the mid
to long term outlook for residential
areas in certain countries. “Places like
Saudi Arabia, Egypt and Abu Dhabi
are places where we are very bullish in
the mid to long term. Yes, they are going
through corrections, they are going
through challenges. Oman, we believe
has opportunities, but one has to look
at the right opportunities,” he said.
<br>
            <br>
            Speaking on the role of the banking
sector, K Gopakumar, General Manager,
Wholesale Banking, BankMuscat
said that there were several positives
for the real estate sector. “In our
opinion real estate sector is positive
for several reasons. The population is
growing significantly and the per capita
income is also going up. The urban
growth and the demographic profile is
also positive,” he said.
<br>
            <br>
            Mansoor Jamal Malik, Managing Partner,
Al Busiady Mansoor Jamal &amp; Co,
spoke on the regulatory framework
in Oman for real estate. According
to him Oman offered both transparency
and security. “A client of mine
in Oman told me that he was keen to
invest in Oman and asked me on security
and stability. I told him Oman offers
both transparency and security,”
Malik said.
<br>
            <br>
            The event concluded with a panel discussion.
The panel members included:
Rajeev Singh, Partner, Ernst &amp; Young
Oman – Transaction Advisory Services; Lo’ai Bataineh, Deputy General
Manager and Head, Investment Management
Group, Oman Arab Bank;
Bruce Palmer, Managing Partner, Curtis
– Oman and Saleh Naseer Abood
Al Habsi, General Manager, Ministry
of Defense Pension Fund, Oman.
<br>
            <br>
            The seminar also saw each of the speakers
and panel members being presented
a memento. Khalil bin Abdullah Al
Khonji, Chairman of Oman Chambers
of Commerce and Industry, inaugurated
Iskan Oman’s website. Khonji was
the chief guest; and Adil Mohammed
Al Bader, chairman of Iskan Oman,
was guest of honour at the seminar.
Oman Economic Review was one of the
media sponsors for the event.</font></p>

