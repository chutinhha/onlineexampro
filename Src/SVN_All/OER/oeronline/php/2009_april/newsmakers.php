			NTI BizPro Awards 2009</font></b><font face="Verdana" size="1"><br>
            <br>
            <img border="0" src="news1.jpg" align="left" hspace="8" vspace="6" width="350" height="252">The much-awaited NTI BizPro Awards for the year 2009 were conferred 
            on recipients at a recent ceremony held at Al Bustan Palace Hotel. 
            Major General Albert E. Whitley WO CMG CBE, was the guest of honour 
            at the event that was also attended by various CEOs and top 
            officials from the corporate world.<br>
            <br>
            The Business Leader for 2009 and the three Young Achievers, who were 
            chosen from among eleven finalists, made individual presentations at 
            the award ceremony to prove their true ability to deserve the 
            prestige and honour.<br>
            <br>
            Qais Mohammed al Yousef, CEO, Al Yousef Group, was announced as the 
            new Business Leader for the year 2009 by His Highness Sayyid Tarik 
            bin Shabib al Said who also handed over the trophy. Steven Thomas, 
            CEO, Renaissance Services handed over the scholarship cheque. 
            Emerging as winners in the Young Achiever’s category were, 
            Abdulnasir N. Al Raisi, Head SME Credit and Marketing, Bank Muscat; 
            Anisa Abdul Rasool Ali Al Raisi, Sales Manager, Hotel 
            Intercontinental and Said Al Noufli, HR Team Leader, Petrogas. <br>
            <br>
            Thomas also congratulated the winners and handed over scholarships 
            to each winner. Each Young Achiever was rewarded a sum of RO2500 and 
            The Business Leader received an amount of RO5000. These scholarships 
            were sponsored by Renaissance Services SAOG.<br>
            <br>
            “Like last year, I still believe this fantastic concept of the 
            BizPro awards can be taken to even higher levels that will attract 
            the leadership of all of corporate Oman and senior government.”, 
            said Thomas while giving away these awards.<br>
            <br>
            To win the NTI BIZPRO Award, participants have to prove their 
            calibre at different preliminary rounds to be eligible to reach the 
            finals. This year too, candidates were hard pressed to prove 
            themselves and the best were finally chosen by experts from KPMG – 
            an independent third party and one of the largest professional 
            advisory services firm in the world spanning more than 140 
            countries. Each of the eleven finalists in the Young Achiever’s 
            category was given a special certificate of excellence by the chief 
            guest.<br>
            <br>
            While giving his speech of acceptance Qais Al Yousef made a very 
            motivational gesture by giving away scholarship award of RO5000 back 
            to NTI to invest in training future business leaders. All the 
            winners shared their joy in being recognised as inspiring leaders of 
            tomorrow and promised to work at the commitment they have been 
            shouldered with.<br>
            <br>
            “The strength of a business leader lies in nurturing his mind 
            towards the intricacies of maintaining decorum in the business 
            world. The NTI has been faithful in nurturing and recognising 
            business leaders and talent. Our sincere support is all we could 
            selflessly extend to see more such leaders emerge in succession to 
            those who have already made a mark for themselves”, said Lawrence 
            Alva, General Manager, NTI. <br>
            <br>
            The NTI BizPro Awards was first launched in 2006 as a social 
            investment initiative by the National Training Institute (NTI) which 
            is one of the leading training providers in the Sultanate. The main 
            objective of these awards is to reward and promote the achievements 
            of the Omani Business Leaders and Young Achievers by acknowledging 
            their contribution to the corporate sectors in Oman. <br>
            <br>
            The various training modules of NTI are designed to ensure that 
            young Omani aspirants acquire the soft skills that enable them to 
            become the preferred choice of employers across the widest range of 
            industrial and services sectors.</font></p>
