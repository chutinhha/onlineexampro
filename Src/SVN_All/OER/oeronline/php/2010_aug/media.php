					research, and publishing company specialising in economic 
					country intelligence, brings out a yearly edition featuring 
					a review of the political and economic developments in the 
					Sultanate. The publication has been one of the key providers 
					of targeted information, analysis and advice to the global 
					business community with regards to investment and business 
					potential.<br>
					<br>
					OBG’s forthcoming 250-page publication, <u>‘The Report: Oman 
					2011’</u>, will provide a review of the key economic sectors 
					with comprehensive information and analysis with regards to 
					investment and business potential in Oman.<br>
					<br>
					<b>How long has OBG been in Oman and how has it performed?</b><br>
					<br>
					OBG has been on the ground for seven years and in this 
					period has developed a very strong relationship with the 
					local business community. The recent memorandum of 
					understanding signed with Brand Oman Management Unit (BOMU) 
					is a testimony to this and recognition of how ‘The Report’ 
					has become a significant resource not only within Oman but 
					further afield.<br>
					<br>
					Additionally our ongoing relationship with our partners Al 
					Busaidy, Mansoor Jamal &amp; Co; and Abu Timam Grant Thornton 
					allows us to provide specific technical advice to our 
					readership. This, coupled with the very strong pool of 
					research sources we have developed over the past seven years 
					is the bedrock of our publication. Indeed, as a ground 
					approach, working with respected partners is one of <br>
					our hallmarks.<br>
					<br>
					We have consistently secured contributions to our Oman 
					publications from His Majesty Sultan Qaboos bin Said; Ahmed 
					bin Abdulnabi Macki, Minister of National Economy; and 
					Sayyid Fahad bin Mahmood Al Said, Oman’s Deputy Prime 
					Minister, to name a few.<br>
					<br>
					<b>What have been the major sectors that have been covered 
					by OBG in Oman so far. Do you plan to cover any new sectors 
					for this year?</b><br>
					<br>
					OBG publications follow a format that our readers have come 
					to know and expect. We provide 250-300 pages of in depth 
					analysis of all the most pertinent sectors of an economy – 
					from a macro-economic and political overview at the 
					beginning to chapter-by-chapter break down of all industries 
					including energy, industry, banking, insurance and tourism.
					<br>
&nbsp;</font><p align="justify" style="margin: 3px 10px" dir="ltr"> 
