					format, we often include chapters which are unique to the 
					country in question. So in Oman we have a regions chapter 
					which particularly focuses on the opportunities and 
					development in Salalah, Sohar and Duqm. As Sohar and Duqm 
					continue to grow there may well come a time when we have 
					specific chapters for each of the regions. Additionally this 
					year, we have added a new chapter on culture, youth, and 
					sports with the assistance of BOMU.<br>
					<br>
					<b>What is the mechanism of information gathering apart from 
					interviews?</b><br>
					<br>
					OBG’s ‘The Report’ draws opinions and thoughts from leading 
					contemporary and influential personalities, both from the 
					government and the private sector, conducting first hand, on 
					the ground research on Oman. The information from interviews 
					makes up the majority of our content. This includes 
					interviewing opinion makers, business leaders and high level 
					governmental decision makers, using their expertise and 
					experience to put together a comprehensive economic guide. 
					To supplement this, we work closely with research partners 
					and access existing data.<br>
					<br>
					<b>How do you overcome the clear and present danger of using 
					outdated data and information in the OBG report?</b> <br>
					<br>
					Indeed, this is a challenge that all of us in the business 
					of information provision deal with, not only in Oman, but 
					much of the rest of the region too. The issue of accurate 
					and up-to-date statistical information is one that is widely 
					debated. We seek to overcome this by coming back annually 
					and having a team on the ground for 6-8 months. We also draw 
					on a lot of material, which we always source and try to 
					ensure it is the most updated available. When we have no 
					choice but to use material that is 12-18 months old, we make 
					it very clear that at the time of printing it was the most 
					recent available. It is pleasing that over the past few 
					years we have seen some very positive improvements in Oman 
					on this front.<br>
					<br>
					<b>What does OBG mean to Omani businessmen and investors?</b><br>
					<br>
					‘The Report: Oman 2011’ will be, as the previous editions 
					have been, a comprehensive and accurate analysis of 
					political, macroeconomic and sectoral developments in Oman, 
					including banking, construction, insurance, capital markets, 
					energy, infrastructure, industry and real estate. Omani 
					businessmen and investors can draw their own conclusions and 
					identify opportunities from the information provided. In 
					addition, ‘The Report’ can be a valuable tool for 
					businessmen to share information about the country with 
					potential partners and investors too, should they wish. It 
					provides information on all major sectors of the economy but 
					also a series of signposts pointing to investment 
					opportunities.<br>
					<br>
					<b>What is the methodology of your circulation and does OBG 
					reach your target audience?</b><br>
					<br>
					‘The Report: Oman 2011’ will be available in print and 
					online version and is available directly from OBG and not in 
					bookstores. Many of our clients buy ‘The Report’ in bulk to 
					distribute to their own customers and investors. The target 
					audience of Oxford Business Group’s Reports is potential 
					investors to a region and judging from the amount of reports 
					purchased by government entities to provide to potential 
					investors, it would appear that they are successful in doing 
					so.<br>
