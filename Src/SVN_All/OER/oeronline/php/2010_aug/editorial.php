				<font face="Verdana" size="4" color="#C00000">Leadership 
				essentials</font><font size="2" face="Verdana"><br>
				</font>
				</b><font face="Verdana" size="1"><br>
				Arising tide raises all ships is an oft repeated phrase in 
				economic parlance. The Oracle of Omaha, Warren Buffet summed it 
				up more graphically, “It is only when the tide goes down that 
				you know who is swimming naked.” A slowdown or any other crisis 
				doesn’t just separate good leaders from the mediocre ones it 
				also often brings out the best in true leaders. Turbulence 
				focuses the mind wonderfully and that is why some of the best 
				ideas come out during a downturn.<br>
				<br>
				In current leadership research a lot of stress is being placed 
				on contingency or situational leadership models. While this is 
				not a single theory but different variations of the same theme, 
				the essential premise of leadership model is that the situation 
				defines the leadership that is required. A leader is successful 
				only if he can change behaviour and style in accordance with the 
				needs of the situation. Quite often, a leader who is superbly 
				successful in one situation may fail completely when the 
				situation undergoes a radical change. Even people who do not 
				agree with this model of leadership agree on one point. Crises 
				requires a very different sort of leadership than normal times. 
				And the global economic crisis tested the leadership mettle of 
				various leaders.<br>
				<br>
				Though there have been lots of busts, slowdowns and recessions 
				in the past, the global economic crisis was unique in the sense 
				that it tested managerial and leadership skills of corporate 
				leaders to the limit. And it created havoc across industries and 
				geographies. <br>
				<br>
				The economic history of the 20th century is often seen in terms 
				of pre-Great depression and post Great Depression. It is likely 
				that when the economic history of the 21st century is written it 
				will be similarly divided into the pre and post Lehman Brothers 
				era’s.<br>
				<br>
				It is not as if there were no boom-bust-boom cycles prior to the 
				Lehman Brother’s bankruptcy. It is simply that the global 
				financial and economic meltdown triggered by the bankruptcy 
				seems to have changed the economic realities of the world 
				forever. The corollary of this argument is that the norms of 
				leading a company in this new world will be very different from 
				the rules that were followed in the older days and this calls 
				for new skills and ideas. <br>
				<br>
				This issue of OER is important not just because it provides 
				insights managing boom-bust cycles better but it’s insights are 
				pretty timeless. The interviews in this issue have thrown up 
				some of the most candid observations on leadership and 
				management, and some rather innovative ideas on how to deal with 
				turbulence.<br>
