            <td width="519" bgcolor="#FFFFFF" valign="top"> <p align="justify" dir="ltr" style="margin-left: 5; margin-right: 5"><br>
                <font face="Verdana" size="4"><b>Oman has a story to tell</b></font><font face="Verdana" size="1"><strong style="font-weight: 400"><br>
            </strong><strong>HE Gary A Grappo, US Ambassador to Oman has been 
            closely involved with the negotiation process of the US-Oman Free 
            Trade Agreement. He speaks about the opportunities created by the 
            agreement and the roadmap ahead in an exclusive interview with 
            Mayank Singh</strong><strong style="font-weight: 400"><br>
            <br>
            </strong><strong>
            <img border="0" src="face.jpg" align="left" hspace="8" vspace="6" width="300" height="337">Your first reactions to the implementation of the 
            US-Oman Free Trade Agreement (FTA)?</strong><strong style="font-weight: 400"><br>
            The US negotiates very few of these. Worldwide we have less than 20 
            of them. We do not have these with some of our closest allies like 
            the UK, Japan and there is a reason for this. FTA’s are very 
            difficult and very comprehensive. They are very long and tedious and 
            it is difficult for many countries to sit down and go through the 
            volumes of commercial and investment laws that each country has and 
            to come to an agreement. One of the remarkable things was that we 
            were able to do this with Oman – the Omani government starting from 
            His Majesty, took a decision that this was important for Oman and 
            its economic development plans and embarked on negotiating this very 
            difficult agreement three years ago and we have succeeded in doing 
            this. I particularly want to point out the efforts put in by HE 
            Maqbool bin Ali Sultan, the minister for commerce and industry and 
            his team in completing this agreement. The FTA is over 800 pages 
            long covering every aspect of business interaction between our two 
            countries. It is very comprehensive and includes telecommunications, 
            intellectual property, financial services, customs, the environment 
            and so forth. We are extremely pleased to have negotiated and 
            implemented this agreement. It is indicative of not only Oman’s 
            plans for economic development but from our perspective a sign of 
            the exceptional relations between our two countries. I feel very 
            strongly that it will serve the interests of both the countries and 
            strengthen the existing exceptional relationship between the US and 
            Oman.<br>
            <br>
            </strong><strong>In your opinion, who are likely to benefit from the 
            agreement?</strong><strong style="font-weight: 400"><br>
            It is important to point out that this is an opportunity to benefit. 
            Any agreement is just an agreement till the affected parties take 
            advantage of it. So it will be the responsibility of businesses in 
            our two countries to capitalise on this and the opportunities are 
            significant for both of them. Now, we have to be practical, the US 
            is a $14trn economy, while Oman is roughly a $40-$45bn economy. So 
            the scales are considerably different, be that as it may there are 
            opportunities for both countries available and what we expect to see 
            is US businesses exploring opportunities in Oman for doing business 
            now that barriers and obstacles have been removed and similarly we 
            want to see Omani businesses capitalise on the opportunity to do 
            business in the US without any hindrance. I strongly feel that Oman 
            has a story to tell and many people in the US would be interested in 
            hearing it.<br>
            <br>
            President Bush announced six years ago his intention to establish a 
            Middle East Free Trade Area. We have negotiated various FTA’s in the 
            region and Oman is the latest step in that process. This was done 
            for two reasons – one, we wanted to create opportunities for our own 
            businesses. Second, FTA’s provide an opportunity for countries to 
            open their markets for people across the world to do business with 
            them. Omanis should look at the FTA as a leverage to attract to 
            attract investors from other countries to business here. The FTA has 
            a clause called – 35 per cent rules of origin, that means that for 
            an Omani product to receive duty free treatment in the US market it 
            must contain 35 per cent Omani content, the rest can be from 
            practically anywhere. Therefore Oman could serve as an export 
            platform to the US for businesses from the region and from other 
            parts. This is a tremendous advantage for Oman.<br>
            <br>
            I would like to take this opportunity to state that we intend to 
            follow up and to provide guidance and assistance to Omani businesses 
            to do business in the US under the FTA. The US embassy will shortly 
            be holding a seminar to on doing business in the US. Apart from 
            experts, individuals from countries with which we have an FTA can 
            talk about their experiences and lessons learnt about the FTA. We do 
            not want this agreement to sit on someone’s shelf, but see 
            businesses take advantage of this. Once this agreement came into 
            effect, its implementation got widespread coverage throughout the US 
            business community. I was receiving e-mails from colleagues with 
            whom I had not spoken for years, saying, ‘Congratulations you did 
            the FTA and is there anything that I need to know?’<br>
            <br>
            </strong><strong>You spoke about the mismatch in scales between the 
            two countries. This has led to apprehensions about US businesses 
            cornering a large part of the benefits accruing from the FTA? How 
            would you respond to this?</strong><strong style="font-weight: 400"><br>
            This is a very common fear in countries with which we negotiate FTAs, 
            it was a fear when we negotiated NAFTA (the North American Free 
            Trade agreement), CAFTA (Central American Free Trade Agreement) 
            individual FTA’s with countries like Chile, Singapore, Australia, 
            Jordan, Morocco, Bahrain and so forth and this fear has never 
            materialised. Businesses are in the business of making a profit, 
            they are not predatory, they are not at moving into markets and 
            taking over. This is not how you do business and that is not the way 
            they want to do business.<br>
            <br>
            What an FTA is that it gives businesses on both the sides, 
            reassurances that they need, like they can conduct business without 
            obstacles and hindrances, with minimum amount of bureaucratic 
            interference, minimum amount of tariffs (and no tariffs and duties 
            on a number of items) and with all the safeguards that they need to 
            have for investments or protection of their products. That’s the 
            real advantage. The government of Oman and the US are saying that we 
            give you all the assurances that you need to do your business, so 
            come and do it. In the other countries that we have negotiated FTA’s 
            this has never happened.<br>
            <br>
            </strong><strong>Is there going to be more competition in some 
            sectors?<br>
            </strong><strong style="font-weight: 400">Absolutely. One of the 
            reasons that we are passionate advocates of FTA’s is that we believe 
            that competition is good for consumers, but also for business. In 
            Chile, for example, the increase in trade with other countries after 
            the FTA was actually greater than its increase in trade with the US. 
            This was because the laws that our countries are required to 
            implement to open their markets apply across the board and so 
            countries which implement an FTA become an attractive place to do 
            business for anyone and not just the US.<br>
            <br>
            </strong><strong>Are there enough safeguards for sectors in which 
            Oman might be weak?</strong><strong style="font-weight: 400"><br>
            Each country is allowed to select certain sensitive sectors at the 
            outset – usually these have to do with national security, which they 
            can set aside. Then in a few other areas, they can negotiate for a 
            necessary transition time, in which the countries can phase in the 
            removal of tariffs. To give you an example, tariffs on all 
            agricultural products from Oman entering the US were lifted 
            immediately from January 1, 2009. However tariffs on 87 per cent of 
            US agricultural products were lifted on January 1, 2009. The 
            remainder will be phased out over five years. This is very normal 
            and there are provisions that take into account how various 
            countries manage their development. <br>
            <br>
            </strong><strong>Are there products in which Oman is well placed to 
            service the US market?</strong><strong style="font-weight: 400"><br>
            We know about Oman’s traditional areas of export and there are 
            definitely opportunities there. When I talk to Omani businessmen, I 
            tell them to explore the US market, either on your own or with 
            partner from the US. The US market is completely open from Miami to 
            Alaska and to New York City. The US is a huge market, it is tough 
            and competitive. As you know these are difficult times, be that as 
            it may, we have seen that when businesses do their homework, study 
            the market they do extremely well, specially since the government is 
            out of the way. An Omani business in the US has the same protection 
            as a local company. <br>
            <br>
            </strong><strong>Trade between the two countries has moved up in 
            anticipation of the FTA? Do you expect trade between the two 
            countries getting further strengthened now? </strong>
            <strong style="font-weight: 400"><br>
            We enjoyed a steady increase in trade over the history of our recent 
            relationship and it has been pretty balanced. In 2007 and 2008 two 
            way trade exceeded two billion dollars. There are two reasons for 
            that, during that period both countries enjoyed significant growth. 
            Second, though the FTA was not implemented in that period many laws 
            were passed in Oman and in the US and businesses could begin taking 
            advantage of these. We have a very good trade relationship and I am 
            confident that it is going to get better.
            </strong>
            <strong>Is it going to happen next week?</strong><strong style="font-weight: 400"> 
            No, it will take sometime, but it will happen.<br>
            <br>
            </strong><strong>Is there a lot of interest amongst US companies 
            about Oman?</strong><strong style="font-weight: 400"><br>
            There will be certainly with the FTA. Let me tell you a small story. 
            When I came here in 1998, before I came to Muscat, a business group 
            organised a meeting for me with companies which would be interested 
            in doing business with Oman, there were six companies which turned 
            up. After six months of my coming to Oman, the same business group 
            organised another business round table conference in New York City, 
            in a fairly large hotel room and there were people who were 
            standing, so definitely there is interest in Oman. I need to temper 
            that comment given the current economic conditions, it will take 
            some time for non-traditional businesses to gain some familiarity 
            and understanding of Oman, but you will see significant interest as 
            the US and global economy starts turning around. We have always 
            said, Oman is a stable and secure country and with the FTA 
            businesses really have no excuse not to be here.<br>
            <br>
            </strong><strong>You spoke about the economic slowdown, do you think 
            that the delay in the negotiation process has resulted in the FTA 
            getting implemented at a wrong time? <br>
            </strong><strong style="font-weight: 400">Certainly, when we started 
            negotiating the FTA, times were quite good. We concluded the 
            negotiations in early 2006, His Majesty and President Bush signed it 
            in the fall of 2006 and then it took us some time to work through 
            the implementation requirements. I do not think that there is ever a 
            bad time for an FTA. However bad things might be, they will be made 
            better by the removal of tariffs, barriers and quotas to trade. So 
            despite the current slowdown, businesses will be well positioned in 
            Oman and the US to take advantage of the situation.<br>
            <br>
            </strong><strong>There is a new government in the US, than the one 
            which negotiated the agreement. Does this take away anything from 
            the FTA?<br>
            </strong><strong style="font-weight: 400">No, this is now a part of 
            US law, as it is here and so even though President Obama took a 
            different approach to free trade during his election campaign, the 
            US-Oman FTA is a matter of law. <br>
            <br>
            <br>
