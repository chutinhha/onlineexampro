		        <td valign="top" width="100%"> <p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
                    </font>
				<b><font face="Verdana" size="4">Battle 
                for the Bulge</font></b><font face="Verdana" size="1"><br>
                <b>Gulf economies are emerging as a stalwart against further 
                deterioration of demand. In general terms, the region was better 
                insulated from the credit crisis fallout than Western economies 
                thanks to minimal exposure to toxic funds and salubrious 
                reserves stemming from a five-year-long oil boom<br>
                <br>
                </b>
                </font> 
				<b>
				<font face="Verdana" size="1" color="#CC3333">By Oliver Cornock</font></b><font face="Verdana" size="1"><b><br>
                </b><br>
                <img border="0" src="economy.jpg" align="right" hspace="8" vspace="6" width="125" height="148">Statistically speaking the last time the global economy was 
                behaving as it is currently was in the midst of a global war. 
                The International Monetary Fund (IMF) has slashed world growth 
                prospects to 0.5 per cent – the lowest since World War II, 
                during which governments around the globe were printing and 
                spending vast amounts of money to aid their respective war 
                efforts. In much the same way, in 2009 public expenditure 
                budgets are ballooning in emerging and advanced economies alike, 
                mimicking historical wartime trends.<br>
                <br>
                Oman’s 2009 budget – with a record deficit – is no exception. 
                But a sustained slump in oil prices might throw a kink into the 
                Sultanate’s development plans at a most crucial time, with both 
                local and international repercussions. While, in the case of 
                Oman, the high ratio of public expenditure to GDP in the new 
                budget is reflective of a commitment to economic diversification 
                rather than a panicked response to the credit crisis, as in the 
                United States, there is little doubt that massive public outlays 
                are necessary in order to re-grease the wheels of the global 
                economy. Indeed, few options to correct the current crisis 
                remain in the private sector and munificent Gulf economies have 
                been targeted as an important life line for global economic 
                recovery. The battle for holistic economic health will thus 
                largely revolve around sticking to development plans in the face 
                of lowering growth prospects, dull oil prices and dehydrated 
                credit markets.<br>
                <br>
                <b>Budgetary numbers<br>
                </b>Oman is increasing total expenditure by 10.8 per cent over last 
                year’s budget and is banking on total revenues increasing by 
                four per cent. This figure factors in a net oil revenue loss of 
                2.4 per cent compared to last year – widely considered a fair 
                estimate, as the 2008 budget used $45 per barrel, well below the 
                actual average, as a base oil price. All told, the projected 
                budget deficit for 2009 is currently $2.1bn. This represents a 
                103 per cent increase over last year’s projected deficit and is 
                equivalent to about five per cent of GDP – a sign of relative 
                strength considering the extraordinary economic times. <br>
                <br>
                <img border="0" src="economy1.jpg" align="left" hspace="8" vspace="6" width="193" height="309">The Sultanate’s expenditure to GDP ratio falls somewhere between 
                the predicted budgetary deficit averages of emerging economies 
                and rich countries. The IMF expects emerging countries, on 
                average, to run a deficit of three per cent of their GDP; it 
                predicts the combined fiscal deficit of advanced economies to 
                equal seven per cent of their GDP, up from two per cent in 2007. 
                From the United States to China, governments are digging deep in 
                their pockets to minimise the economic damage. <br>
                <br>
                Gulf economies are emerging as a stalwart against further 
                deterioration of demand. In general terms, the region was better 
                insulated from the credit crisis fallout than Western economies 
                thanks to minimal exposure to toxic funds and salubrious 
                reserves stemming from a five-year-long oil boom. A willingness 
                to spend on the part of the region should bolster the recovery 
                of the wider global economy, as Masoud Ahmad, director of IMFs 
                Middle East and Central Asia Department noted in a February 8 
                conference in Dubai. “By continuing to spend, oil-exporting 
                countries are contributing substantially to supporting global 
                demand and are acting as stabilisers during the global 
                downturn,” said Ahmad.<br>
                <br>
                But given that the hoped-for reversal in oil price decline has 
                not yet arrived as of February and oil price forecasts continue 
                to be revised in the negative direction, the efficacy of 
                oil-exporting economies in keeping global demand buoyant may 
                undergo yet another process of revision. <br>
                <br>
                <b>Growth projections</b><br>
                The IMF has adjusted its estimates for world economic growth 
                four times in the last nine months as the economic situation has 
                continually worsened. In its most recent revision at the end of 
                January, the IMF predicted real GDP growth in the GCC region to 
                slow from 6.8 per cent in 2008 to 3.5 per cent in 2009, citing 
                the declining demand for oil as the primary reason.<br>
                <br>
                The IMF also cut its estimate for average oil price in 2009 – 
                changing its previous prediction in November from $68 per barrel 
                to $50 per barrel (as a simple average of WTI, Brent and Dubai 
                Sweet Crude). On February 10 the Energy Information 
                Administration (EIA) of the United States marginally lowered its 
                average oil price forecast for 2009 from $43.25 to $43.14 per 
                barrel for West Texas Intermediate (WTI), the most common 
                international measure used. <br>
                <br>
                For the coming fiscal year Oman’s budget was written on an 
                assumed oil price average of $45 per barrel. Using the EIA’s 
                average price and Oman’s expected oil output of 805,000 barrels 
                per day (bpd), the Sultanate will be coming up $1.5mn below 
                expectations on each day of 2009, on top of the existing $2.1bn 
                deficit.<br>
                <br>
                The minister of national economy, HE Ahmed bin Abdulnabi Macki, 
                has already indicated that should market prices continue to dip 
                below $45 per barrel, officials would cut public spending. 
                Speaking to local press shortly after the unveiling of the 
                budget in early January, HE Macki said they would “reorganise 
                and restructure our projects but we are flexible in our budget 
                for such an eventuality.” Rather than touch government salaries, 
                said HE Macki, officials would “rearrange our priorities as far 
                as the projects are concerned,” referring to the swathe of 
                investment initiatives under the Vision 2020 plan. <br>
                <br>
                As a rather gloomy economic outlook remains resistant to change 
                and a budget reassessment becomes more likely, the question of 
                which public projects will land at the top of the priority list 
                will inevitably arise. More so than ever before, the 
                international investment community will be paying close 
                attention to Oman’s decisions as sources of demand growth are 
                increasingly hard to come by in the West.<br>
                <br>
                Meanwhile, Oman and other GCC economies will have to strike a 
                delicate budgetary balance between maintaining acceptable levels 
                of growth in the short term and continuing to develop a hearty 
                role for the non-oil sector in the long term. So far, the 
                Sultanate appears to be doing just this.<br>
                <br>
                <b>Outlays in place</b><br>
                Keeping in line with the Oman Vision 2020 plan, investment in 
                human capital plays a prominent role in the latest budget. The 
                health and education sectors have both received notable 
                increases in government funding, up by 18.9 per cent and 11.4 
                per cent, respectively, from the 2008 budget. <br>
                <br>
                At the same time gas production investments increased by 52 per 
                cent year-on-year, from RO50mn to RO76mn. Again, this boost in 
                spending aids Oman’s diversification plan, which calls for a 
                stronger role for the gas sector. Oil production outlays rose by 
                8.9 per cent to RO196mn as the Sultanate reaches for a 
                production target of 805,000 bpd, up from 713,000 bpd in 2007, 
                mainly through enhanced oil recovery techniques.<br>
                <br>
                But, as ever in this period of economic travail and constant 
                re-assessment, these figures are subject to change. Until oil 
                demand perks up or OPEC production cuts force prices up, Oman is 
                operating on revenues below budgeted levels and may cut 
                initiatives. Should some public spending ultimately be revised 
                on account of weak oil demand, budgetary cuts could be magnified 
                in the midst of the economic battle – both domestic and 
                international – for growth. </font> <p align="justify" style="margin: 3px 10px">
