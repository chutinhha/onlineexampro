		        <td valign="top" width="100%"> <p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
                    </font><font face="Verdana" size="4"><b>
                Will 
                Yellow Mellow? <br>
                Gauge the New Gold Rush<br>
                </b></font>
                <b>
                <font face="Verdana" size="1">As a rule of thumb, owning one to three per cent gold in your 
                portfolio is not unreasonable, inversely proportioning it with 
                price. When gold prices touch 30 per cent or more below its 
                $650-700 baseline, own more of it, and as prices creep well 
                above fair value, bring down allocation<br>
&nbsp;</font></b><p align="justify" style="margin: 3px 10px">
                    <font color="#CC3300"><b><font face="Verdana" size="1">By 
                Rehan Syed</font></b></font><font face="Verdana" size="1"><br>
                <br>
                <img border="0" src="invi1.jpg" align="right" hspace="8" vspace="6" width="90" height="106">The yellow metal is white hot. Of late, the media has been flush 
                with talk of gold soaring to $1,200 per troy ounce, if not more. 
                With it currently trading at $960, up 35 per cent in a quick 
                three months but down five per cent from last year’s high, 
                should investors queue up for this additional 25 per cent gain, 
                which in a time of trashed markets is indeed a golden return? Or 
                is it a case of inflated return expectations where the 
                investment rationale centers primarily, and possibly speciously, 
                on a quick return to an inflationary era? <br>
                <br>
                <b>Goldbugs lack of faith <br>
                </b>Two of the three hallmarks of the 1970s attempted a comeback 
                in recent years, disco and bell bottoms; will core inflation be 
                the third? Only in that scenario does it make sense to chase 
                gold from its current elevated levels. And if we struggle 
                against a deflationary trap, as has Japan for the past ‘lost’ 
                decade, gold will struggle to hold on to its recent gains. Both 
                these scenarios reflect the extremes of the inflation-deflation 
                spectrum, whereas central bankers strive for a happy middle, 
                where aggressive fiscal monetisation enthuses the economy 
                followed by a timely withdrawal of surplus stimulus, all without 
                disrupting trend growth, altogether a magical performance if it 
                succeeds. <br>
                <br>
                The goldbugs, as gold enthusiasts are known, are counting on the 
                Bernanke helicopter to fly high and leave late, which is to say 
                they place faith in the US central bank to jump start the 
                economy by printing money, but they doubt its ability to quickly 
                mop up the excess money supply before inflation takes root. As 
                economists of the Austrian school accurately assert, all 
                episodes of past inflation have been preceded by loose money 
                policies. But as the un-Austrian and now fashionable Keynes 
                said, the gold standard is a ‘barbarous relic’ and stimulus 
                spending is essential to power us out of this recession.<br>
                <br>
                My view is that given the weighty arguments underpinning the 
                inflation-deflation debate, the lack of conviction around either 
                view, and the large impact of the outcome, we should overweight 
                the view that is not priced into the market. Is this an artful 
                dodge? Possibly, but it is a practical one since most economists 
                have failed in the past to call the big turns between business 
                cycles.<br>
                <br>
                <b>Historical underperformer<br>
                </b>Including the 250 per cent rise in gold price from a bottom 
                of near $250 in 2001, the compound return has been near 14 per 
                cent per annum (p.a.) this decade, versus a loss of almost 5 per 
                cent p.a. for the S&amp;P 500 stock index. However, this decade’s 
                gain is far above the nine per cent p.a. return since the $35 
                regulated price of 1971, the year in which the US closed the 
                gold window and subsequently abandoned the gold standard, 
                rivaling the similar but uncorrelated nine per cent p.a. return 
                for the S&amp;P during that period. <br>
                <br>
                Since Federal Reserve policy experienced a sea change in 1979 
                from passive to active on inflation containment, that year is a 
                better starting point for comparison. Both in the 1980s and 
                1990s, gold fell about 2-3 per cent p.a. vs. a gain of 17-18 per 
                cent p.a. for the S&amp;P index, a 20 per cent underperformance. 
                This disparity is exaggerated by end point sensitivity; shifting 
                end points by a year still results in S&amp;P outperformance, but of 
                much reduced magnitude. Keep in mind volatility of gold is 15 to 
                20, equal to or slightly higher than that of the S&amp;P 500 equity 
                index, depending on the timeframe examined. While weak gold 
                performance in the 1980s may be dismissed as a bursting of the 
                1970s gold bubble, its lack of luster in the 1990s is harder to 
                explain away. <br>
                <br>
                Finally, with this decade’s strong return, gold markets are 
                clearly pricing in more of an inflationary than deflationary 
                outcome. This is in contrast to the Treasury Inflation Protected 
                Securities (TIPS) market, which is possibly a better predictor 
                of inflation and which is currently signalling the lack of it. 
                While the ten year treasury yield has no doubt spiked from a low 
                of 2.05 per cent last November to 2.7 per cent, this has been 
                more due to anticipated excess supply of paper rather than 
                evidence of inflation.<br>
                <br>
                <b>The goldbug’s gamble</b><br>
                The goldbug’s premise that inflation will appear over the next 
                year implies deflation will end, an event which will have a 
                strong positive effect on many oversold asset classes, including 
                high-grade credit and equities, possibly more so than on gold. 
                At least half of this inflation bogeyman story is already priced 
                into gold, given its recent rapid 35 per cent rise, driven by a 
                rush toward gold ETFs and physical gold. While wider acceptance 
                of this could drive gold a bit higher, the risk/reward tradeoff 
                is clearly not as favourable as at the outset.<br>
                <br>
                Another factor in favour of higher gold is a declining supply 
                base, which now mines or recycles 2,200 metric tonnes per year 
                versus a peak of 2,700 in 2002, representing a three per cent 
                per annum decline. Supply has been held down due to rising cash 
                production cost and chronic under-investment owing to depressed 
                prices earlier this decade, similar to the situation in the oil 
                industry, but will likely rise as miners increase investment 
                following price rises; however, it takes at least five years to 
                bring new supply online.<br>
                <br>
                Finally, there is a group of die-hard goldbugs who believe that 
                gold price would be dramatically higher were it not for the fact 
                that central banks conspire to vastly exaggerate their holdings, 
                similar to the peak oil theorists who opine oil producers have a 
                lot less oil underground than they claim but lack of 
                transparency renders it difficult to prove otherwise. For now, I 
                ignore these marginal views.<br>
                <br>
                <b>The realist’s seven reservations</b><br>
                There are good reasons to reign in some of the recent goldbug 
                optimism. First off, since gold is a store of value, what is the 
                fair value for gold? Various currency strategists peg this 
                around the $650-700 mark, and that of the EUR/USD fair value 
                exchange rate between 1.15-1.20.<br>
                <br>
                </font>
                <img border="0" src="invi.jpg" align="right" hspace="8" vspace="6" width="368" height="326"><font face="Verdana" size="1">Second, much of the recent investment demand is based more on 
                fear of systemic financial collapse than inflation. When this 
                fear subsides later this year, it will take profit.<br>
                <br>
                Third, unlike in the 1970s, investors can immunise against 
                inflation via inflation linked securities like US TIPS, an 
                option that did not exist in the 1970s. TIPS are more attractive 
                that straight treasurys at present and are better than gold at 
                immunising income, but the downside is reinvestment risk.<br>
                <br>
                Fourth, commodity markets offer a better inflation hedge, but 
                suffer in deflationary times, as seen recently.<br>
                <br>
                Fifth, jewellery demand, which accounts for two-thirds to 
                three-quarters of gold consumption is under pressure, as is the 
                rest of discretionary consumer spend, but was seasonally strong 
                recently due to the holiday season of Indian Diwali, Christmas 
                and Chinese New Year. This matters because India is the world’s 
                largest gold consumer, China’s demand is growing, and both 
                offset weak industrial and US consumer demand.<br>
                <br>
                Sixth, central banks have been consistent sellers, offloading 
                somewhere between 300 to 500 tonnes per year of their vast 
                inventory of 30,000 tonnes. Given mounting fiscal deficits, it 
                is likely that sales will run at the high end of this range 
                which limits gold price rise.<br>
                <br>
                Seventh, gold producers regularly borrow gold from central banks 
                and sell it against future production, a form of production 
                hedging to smooth out realised price, and there is more 
                incentive to sell when prices are high, thus braking the price 
                rise.<br>
&nbsp;</font><p align="justify" style="margin: 3px 10px">
                <font face="Verdana" size="1">
                <b>One bar does not fit all</b><br>
                How many gold bars to own to hedge against the bugaboos of 
                deflation and rampant inflation, while not getting caught up in 
                the periodic exuberance of the diehard goldbugs? Asset 
                allocation holds the answer. An academically rigorous exercise 
                in mean variance optimisation and efficient frontier allocation 
                would likely prescribe very low gold ownership levels of 1 per 
                cent or so and commodity exposure below 5 per cent. However, the 
                record macroeconomic uncertainty diminishes the value of such 
                precision.<br>
                <br>
                As a rule of thumb, I think owning 1-3 per cent gold in your 
                portfolio is not unreasonable, inversely proportioning it with 
                price. When gold prices 30 per cent or more below its $650-700 
                baseline, own more of it, and as prices creep well above fair 
                value, dial down allocation. For the fearful, it is worth noting 
                that in the years before the second world war, many wealthy 
                European families held 20 per cent of their assets in gold, 
                which served them very well. I am discounting the return to such 
                extreme scenarios.<br>
                <br>
                How to own yellow? Coins and gold bars might have a satisfying 
                feel and satisfy flight to safety impulses in troubled times, 
                but they offer poor value given a fabrication markup which is 
                currently at elevated levels given the supply-demand imbalance. 
                Pooled or segregated accounts available at some banks are the 
                cheapest way to directly own bullion and, if necessary, physical 
                possession can be arranged at extra cost. Mainstream options 
                include ETFs like the iShares Comex Gold Trust, which have the 
                solace of physical backing. Scaremongers dismiss the physical 
                backing as phony, however I believe that fear is without merit 
                since they are audited and the backing is held in recognised 
                depositaries in major money centres.<br>
                <br>
                Another option is to own shares of gold producers like GoldCorp 
                or Barrick. However, these shares are very volatile relative to 
                the metal itself, because the substantial extraction cost of 
                gold results in high operating margin fluctuation and also since 
                the sub-$200B market cap of the sector is tiny, resulting in the 
                shares being whipsawed by fund flows. So, unless you are a 
                speculator, better to have a professional fund manager allocate 
                to them. <br>
                <br>
                All said and done, gold is one of many appropriate hedges 
                against extreme events and eventual inflation. In particular, to 
                offset the latter risk, adopt a diversified strategy which 
                includes TIPS, other commodities and hard assets like real 
                estate or low-geared REITs.
