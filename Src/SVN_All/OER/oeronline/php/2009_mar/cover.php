			<p align="center" style="margin-left: 10px; margin-right: 10px"><font face="Verdana" size="1"> 
                <br>
                </font>
            <font size="1" face="Verdana, Arial, Helvetica, sans-serif">
            <b><img border="0" src="cover14.jpg" width="400" height="462"></b></font></p>
			<p align="justify" style="margin-left: 10px; margin-right: 10px">
            <font size="1" face="Verdana, Arial, Helvetica, sans-serif">The numbers speak for themselves – the global market for Islamic 
            Financial services (based on Sharia complaint assets) is estimated 
            to be in excess of $700bn. Though this is a relatively small 
            fraction of the total assets amassed by the global banking industry, 
            the sustained growth rate of Sharia complaint assets over the last 
            decade eclipses their conventional counterparts. According to ‘The 
            Banker’ magazine, the growth in Sharia compliant assets in 2007 was 
            29.7 per cent compared to a 16.3 per cent average growth of the top 
            1000 banks worldwide. The Islamic Finance (IF) industry has grown at 
            an enviable 15-20 per cent per annumn over the last decade.<br>
            <br>
            The equity markets mirrors this trend. In 2008, the S&amp;P (Standard &amp; 
            Poor’s) 500 index fell by 38.5 per cent while the Dow Jones 
            Industrial Index showed a record decline of 33.8 per cent. In 
            contrast, the Dow Jones Islamic Financial Index showed a more modest 
            loss of seven per cent for the year. A Q2, 2008 S&amp;P report showed 
            their world benchmark to have fallen by 1.5 per cent, while stocks 
            deemed to be in compliance with Islamic Investment principles 
            delivered returns of 3.6 per cent. Says Sumeet Nihalani, senior 
            director sales, Dow Jones Indexes, Asia-Pacific and Middle East, 
            “Islamic investments have shown a tendency to outperform their 
            conventional counterparts in longer terms.”<br>
            <br>
            Continuing demand in the 1980s and sustained growth in the 1990s, 
            has meant that the market for Islamic financial products has 
            attracted the attention of a number of commercial banks. Today, 
            there are over 300 IF institutions in more than 75 countries across 
            the globe. Says Rajeev Singh, partner, transaction advisory 
            services, Ernst &amp; Young, Oman, “IF is one of the fastest growing 
            areas of banking – in terms of both the availability of new products 
            and in geographical spread.”</font></p>
			<p align="justify" style="margin-left: 10px; margin-right: 10px">
            <img border="0" src="cover1.jpg" width="475" height="299"></p>
			<p align="justify" style="margin-left: 10px; margin-right: 10px">
            <font size="1" face="Verdana, Arial, Helvetica, sans-serif">
            <b>Spreading its wings <br>
            </b>International banks like HSBC, Citibank, Standard Chartered and 
            Barclays have incorporated IF in their portfolio. “There has been an 
            unprecedented interest in IF in recent years, and the increasing 
            number of global banks embracing Sharia-compliant banking in some 
            shape or form clearly attests to growing global demand and 
            opportunities that exist,” says Falah N Al Falah, CEO and managing 
            director, Unicorn Capital, Dubai.<br>
            <br>
            A number of conventional financial institutions have participated in 
            large IF transactions over the past several years. Unicorn, for 
            example, has worked closely with several major conventional banks, 
            including WestLB, Deutsche Bank and Standard Bank, on several sukuk 
            issues, including two sukuk’s for Dar Al-Arkan Real Estate 
            Development Company in Saudi Arabia with a total value of $1.6bn, 
            and a $150mn issuance for Investment Dar of Kuwait.</font></p>
			<p align="justify" style="margin-left: 10px; margin-right: 10px">
            <font size="1" face="Verdana, Arial, Helvetica, sans-serif">
            <img border="0" src="cover2.jpg" width="475" height="338"></font></p>
			<p align="justify" style="margin-left: 10px; margin-right: 10px">
            <font size="1" face="Verdana, Arial, Helvetica, sans-serif">
            The trend is not just limited to institutions as various countries 
            are vying to position themselves as a hub for IF. Says Alan Shanks, 
            partner, banking and finance, HBJ Gateley Wareing (a law firm), 
            London, “Western countries such as UK and France have embraced IF 
            and Britain has actively sought to be seen as a centre of IF.” Qatar 
            Islamic Bank recently obtained an authorisation from the European 
            Finance House and the UK Financial Services Authority to operate as 
            an Islamic Investment Bank in the UK.<br>
            <br>
            <img border="0" src="cover3.jpg" align="left" hspace="8" vspace="6" width="150" height="233">In Asia, Japan, Hong Kong and Singapore are laying out the red 
            carpet for IF. Singapore has stepped up its efforts to develop the 
            industry by streamlining its tax and regulatory regime. In 2006 the 
            Monetary Authority of Singapore (MAS), changed certain tax rules and 
            regulations that worked against Murabaha (see box on Islamic 
            Products). The SGX (Singapore Exchange) has developed Sharia 
            compliant indices along with UK’s FTSE.<br>
            <br>
            Singapore’s 2008 budget gave tax incentives on income derived from 
            specific Shariah compliant activities, offshore Islamic insurance (takaful) 
            or reinsurance (retakaful) business. In January 2009, MAS launched a 
            $134mn (200mn Singapore dollars) Islamic bond programme called Sukuk 
            Al Ijara, to promote the growth of IF in the city state. Says Vince 
            Cook, CEO, The Islamic Bank of Asia, “Singapore is a convenient hub 
            for cross-border transactions, and a convenient intermediary for 
            capital flows between Middle East and Asia.”</font></p>
			<p align="justify" style="margin-left: 10px; margin-right: 10px">
            <img border="0" src="cover15.jpg" width="475" height="303"></p>
			<p align="justify" style="margin-left: 10px; margin-right: 10px">
            <font size="1" face="Verdana, Arial, Helvetica, sans-serif">
            <img border="0" src="cover4.jpg" align="right" hspace="8" vspace="6" width="175" height="302">A report by The Banker in 2008, showed that the top countries by 
            Shari’a compliant assets were Iran, Saudi Arabia, Kuwait, UAE and 
            Bahrain from the Middle East and Malaysia, Brunei and Pakistan. 
            Interestingly, UK was placed 10th on the list.<br>
            <br>
            Overall, Bahrain and Malaysia have the distinction of being the most 
            well established centres of IF. Says Sameer Abdi, partner, advisory 
            services, Ernst &amp; Young, Bahrain, “Both these countries boast of an 
            advanced regulatory environment and proactive stance towards the 
            industry.” Organisations such as the Accounting and Auditing 
            Organisation for Islamic Finance Institutions (AAOIFI) in Bahrain, 
            and the Islamic Financial Services Board (IFSB) in Malaysia exert an 
            influence far beyond their own jurisdictions and actively engage 
            with other international bodies.<br>
            <br>
            <b>Takeaways from the model<br>
            </b>The global financial crisis and the fall of hallowed 
            institutions like Lehman Brothers, Merrill Lynch, Fannie Mae, 
            Freddie Mac, Bear Stearns and Citi Group in the wake of the sub 
            prime crisis, and the resilience of IF (to a large extent) to the 
            meltdown has led to calls for conventional banks to learn a few 
            things from the latter. Says Shanks, “If you compare the ethics of 
            Islamic compliant lending with some American sub-prime lending you 
            will find that IF is much more vigorous as speculation is forbidden 
            and risk must be shared between the borrower and financier.”<br>
            <br>
            It could be argued that Islamic banking and finance have largely 
            escaped the worst of the financial crisis as they are not 
            well-entrenched in the worst affected economies. Additionally, the 
            fact that it is a relatively new phenomenon has meant that it has 
            perhaps not reached the same stage as conventional finance in terms 
            of innovation and advanced market infrastructure, such as secondary 
            markets. Thus Islamic Finance Institutions (IFIs) have not been able 
            to participate in the type of transactions which their conventional 
            counterparts have been exposed to.<br>
            <br>
            <img border="0" src="cover5.jpg" align="left" width="221" height="247" hspace="8" vspace="6">However, what is apparent is that some of the abiding principles 
            that underlie IF, have restricted IFIs involvement in some of the 
            investments and practices that have exacerbated the crisis. At the 
            epicenter of the global financial disaster were lending practices 
            that led to the creation of the sub-prime mortgage fiasco. IFI’s can 
            finance home purchase, and in theory could be exposed to sub prime 
            lending. However, the inherent risk management within most IFIs 
            would in all likelihood have meant that greater initial deposits 
            would have been required, thereby automatically restricting their 
            exposure. Furthermore the conservative nature of most IFI’s, means 
            that they tend to lend less as a percentage of assets and deposits, 
            holding higher capital reserves compared to conventional banks.</font></p>
			<p align="justify" style="margin-left: 10px; margin-right: 10px">
            <font size="1" face="Verdana, Arial, Helvetica, sans-serif">
            <b>A matter of principle<br>
            </b>What has really pushed the global economy over the precipice has 
            been the bundling-up of these assets and the subsequent trading of 
            debt securities in secondary markets, through often complex 
            derivative instruments such as collaterised debt obligations (CDO’s). 
            Says Tayyebi Aziz, financial reporting officer, Association of 
            Chartered Certified Accountants (ACCA), London, “IF stresses the importance of underlying assets to back transactions.” This 
            inherently limits the ability of IFIs to trade in debt securities 
            and certainly restricts excessive leveraging through the repackaging 
            and selling of that debt. “While there are concerns that some IF 
            securities have moved away from this basic premise, by attempting to 
            mirror conventional products, it is generally accepted that diluting 
            the link to the original underlying assets is looked upon with 
            suspicion by most Islamic scholars,” adds Aziz. For this reason, it 
            would be highly unlikely that many of the derivative instruments 
            which have driven the unsustainable credit bubble over the decade or 
            so would have been passed by the Sharia Supervisory Board’s (SSBs) 
            of most IFIs.</font></p>
			<p align="center" style="margin-left: 10px; margin-right: 10px">
            <img border="0" src="cover6.jpg" width="418" height="378"></p>
			<p align="justify" style="margin-left: 10px; margin-right: 10px">
            <font size="1" face="Verdana, Arial, Helvetica, sans-serif">
            <img border="0" src="cover7.jpg" align="right" width="200" height="281" hspace="8" vspace="6">Contractual risk too is forbidden within Islam. In general, this 
            prohibits the selling of goods and services that the seller is not 
            in a position to deliver or the making of a contract which is 
            conditional on an unknown event. Thus a person cannot sell something 
            that he does not own. While there are some prohibitive exceptions, 
            this again limits IFIs from dealing in certain derivatives such as 
            conventional futures contracts. In particular, the rise in 
            speculative trading in these instruments that has flourished since 
            the demise of delivery-settled to cash-settled contracts, would be 
            prohibited under IF.<br>
            <br>
            Similarly, the price and nature of goods being transacted are 
            defined in detail and agreed upon by both parties in IF, thereby 
            avoiding a sale that may represent a gamble. For example, 
            conventional short sales or sales on margin are prohibited. The 
            damage that such trading can do to the efficiency of markets has not 
            gone unnoticed by regulators around the world – the Securities and 
            Exchange Commission (SEC), temporarily restricted such trading 
            during 2008 in order to bring stability to the markets.<br>
            <br>
            <b>Sukuk as a prime mover <br>
            </b>The emergence of sukuk instruments (sometimes referred to as 
            ‘Islamic bonds’) has changed the nature of Sharia-compliant debt 
            securities and added to IF’s growth on a global scale. Sukuk 
            transactions have been used in countries like Germany, Japan and the 
            US. While there is still some controversy within the IF industry, in 
            terms of the structure that a sukuk should take, and the role of 
            secondary markets, sukuk’s are set to emerge as an important 
            congruent of the global Islamic financial system. Global sukuk 
            issuance registered a CAGR of 116.9 per cent from 2002-2007. Total 
            sukuk issuance at the end of 2007 standing at $47.1bn. According to 
            a S&amp;P forecast, the sukuk market will surpass $100bn by the <br>
            end of 2010. <br>
            <br>
            <b>
            <img border="0" src="cover8.jpg" align="left" hspace="8" vspace="6" width="180" height="291"></b>However, in 2008, the uncertainty in the global capital markets also 
            manifested itself in the sukuk market which showed a marked decline. 
            Nonetheless, reports by BNP Paribas and S&amp;P suggest that issuances 
            would recover beyond 2007 levels in 2009.<br>
            <br>
            <b>Growth drivers<br>
            </b>The internationalisation of financial markets and the growth of 
            Middle East wealth following the oil boom of the 1970s fuelled the 
            demand for IF. In the recent past, a boom in oil prices resulted in 
            large amounts of liquidity being generated, in the Gulf region 
            aiding the growth of IF. Says Nihalani, “This has led to a strong 
            growth of Muslim wealth, and increasingly, these Middle Eastern 
            investors, following their Sharia mandate, started to relocate 
            assets from conventional to Sharia-compliant <br>
            investments.”<br>
            <br>
            Furthermore, the prosperity of emerging countries with a high Muslim 
            population has also contributed to increased demand for Islamic 
            banking. And finally, the recent efforts of various countries to 
            promote Islamic banking to attract the Middle East’s burgeoning 
            wealth has aided its growth. Says Shanks, “As western economies 
            begin to move into recession and the impact of the global credit 
            crunch is felt in real economies around the world, the oil-powered 
            liquidity of the Middle East continues to drive economic activity at 
            home and abroad.”<br>
            <br>
            A renewed awareness of a faith based concept resulted in increased 
            undertaking of theoretical work and research, from the 1970s, to 
            understand whether a banking/economic system could function without 
            ‘interest.’ This has helped in making the IF model more acceptable 
            and sophisticated. “The growing emphasis within the industry on 
            providing well structured and competitive products has been an 
            important driver for IF,” says Al-Falah.<br>
            <br>
            <b>Roadblocks on the way<br>
            </b>
            <img border="0" src="cover9.jpg" align="right" width="185" height="292" hspace="8" vspace="6">According to a McKinsey report in January 2009, IF faces three 
            main challenges – limited sources of liquidity, high exposure to 
            real estate and a slowdown in primary sources of revenue. In the 
            absence of a viable Islamic interbank market customer deposits work 
            as a primary source of funding for Islamic banks. Apart from 
            limiting their ability to raise capital, this also creates an asset 
            liability mismatch. “IFI’s have short term deposits but long term 
            lending, and this is a big risk,” says Singh.<br>
            <br>
            As a matter of principle all IF transactions need to have an asset 
            backing. Given the restrictions on the type of investments that they 
            may hold, IFI’s have tended to hold less diversified investment 
            portfolios than conventional banks. While this may have limited 
            their exposure to many of the toxic assets associated with the 
            global financial crisis, the value of their balance sheets are 
            extremely vulnerable to falling market values in two particular 
            asset classes, namely equity portfolios and real estate. As the 2008 
            results start getting published, the extent of their exposure to 
            dipping equity markets will become more apparent. However perhaps 
            more worrying is what could happen if house prices in countries 
            where IF is prevalent, begin to erode. With signs of the property 
            boom in countries like the UAE and Malaysia appearing to wane, 
            Islamic banks there may look far less comfortable. The tell tale 
            signs are already there – according to estimates, the sukuk market 
            for 2008 is down by 70 per cent over 2007.<br>
            <br>
            <img border="0" src="cover10.jpg" align="left" width="216" height="314" hspace="8" vspace="6">There are some concerns about their size. As stated earlier, the 
            size of assets held by Islamic banks are still well below 
            conventional banks. Thus Islamic banks need to think about creating 
            synergies by forming small syndications or club deals, to address 
            the large funding needs of clients. Otherwise, these individual 
            banks run the risk of being marginalised.<br>
            <br>
            Says Abdi, “In the current economic climate, two challenges stand 
            out – effective risk management and operational efficiency.” The 
            first, effective risk management, often lacks during periods of 
            rapid growth - this is particularly true for Islamic banks where 
            concentrations and counter party risks have not been fully accounted 
            for. The second, operational efficiency, has been offset by top line 
            growth and led to the corresponding lack of process efficiencies and 
            technology sophistication across operating models.<br>
            <br>
            The lack of adequate number of skilled personnel is another 
            operational hurdle. “A shortage of skills amongst bankers and in the 
            advisory community is slowly being addressed, though the number and 
            availability of Islamic scholars is also an issue,” says Shanks.<br>
            <br>
            There is no global consensus on Sharia and a lack of standardisation 
            tends to result in bespoke packages that often results in higher 
            transaction costs. To cite a recent example, the IF industry bans 
            banking structures that are vague or ambiguous to avoid exploitation 
            – a rule which some argue shuts out the use of common hedging 
            instruments such as currency and interest rate swaps and futures 
            contracts. But as more markets embrace IF, and the need for risk 
            protection increases, there are growing attempts to find Sharia 
            hedging tools. Analysts believe that the industry presently has few 
            instruments to guard against wild swings in currency and interest 
            rate movements, endangering its future.<br>
            <br>
            <img border="0" src="cover11.jpg" align="right" width="216" height="302" hspace="8" vspace="6">Given diverse interpretations of Islamic law, the industry is 
            divided over the use of derivatives. Conservative religious scholars 
            reject derivatives because hedging practices are deemed to be 
            speculative bets on currency and stock movements, which violate the 
            Sharia ban on gambling. Derivatives are also avoided as their 
            underlying assets can be uncertain, as many loss-laden western banks 
            and investors have discovered.<br>
            <br>
            Other Sharia advisers permit derivatives as long as they are used to 
            hedge risks on existing investments and not for speculation. Thus IF 
            contracts are subject to varying interpretations due to different 
            readings of the Sharia. This ambiguity is expected to reduce as 
            product standardisation comes, but as of now it is still some way 
            off.<br>
            <br>
            However, these issues are surmountable. The increasing influence of 
            Middle East on the global economy and the enthusiasm with which 
            established financial centres are embracing IF, ensures its 
            continued popularity and growth. <br>
            <br>
            <b>Time for a rethink? <br>
            </b>Interestingly, and uniquely Oman is the only country amongst its 
            neighbours, that does not allow local banks to deal in IF. Says Aziz, 
            “The fact that Sharia compliant transactions are taking place, 
            suggests that there is an appetite both at a conscientious level, as 
            well as purely business-minded level within Oman. This gap is 
            essentially being bridged by regional banks that have successfully 
            provided sources for IF transactions.” It is quite apparent that the 
            Oman government seeks to maintain a clear distinction between 
            business and religion.<br>
            <br>
            Adds Aziz, “In this respect, Malaysia, where IF operates on an equal 
            footing with conventional finance, and the burgeoning UK market 
            where the UK Financial Services Authority (FSA) has ensured IF is 
            not distinguished from conventional financing in legislation are 
            good models for Oman to consider.”<br>
            <br>
            <b>Promising future </b><br>
            Says Mark Stanley, assistant manager, advisory services, Ernst &amp; 
            Young, Bahrain, “Islamic banking has paradoxically been both 
            galvanised and humbled by the global financial crisis.” Sharia 
            compliance has prevented exposure to many of the investments that 
            severely impaired conventional banks – a fact heralded by advocates 
            for the industry. However, at the same time, the crisis has 
            stress-tested Islamic banking and highlighted significant industry 
            and asset class concentrations, which, if unchecked, could impede 
            sustainable growth.<br>
            <br>
            Says Al-Falah, “IFI’s have increasingly demonstrated that they have 
            the ability to compete effectively with conventional financial 
            institutions and offer competitive and well-structured products and 
            services, thus the industry will achieve new frontiers in terms of 
            product innovation, sophistication and marketshare.” Islamic banking 
            has been maturing from its niche to become a more prominent player 
            in the financial services arena.<br>
            <br>
            A number of Islamic banks are hailing the current financial crisis 
            as a ‘golden opportunity’ for IF as it will highlight the advantages 
            of using Sharia principles. The enthusiasm with which conventional 
            western banks have rolled out Sharia-compliant products strengthens 
            this argument.<br>
            <br>
            Looking back, it is clear that there are some inherently stabilising 
            factors within IF, which have capped the impact of the current 
            market turmoil. As regulators and other stakeholders seek to manage 
            the current crisis, and put in place a framework to ensure something 
            similar does not happen again, there are clearly a number of 
            factors, like the importance of trading in an ethical manner, that 
            are inbuilt in IF which provide suitable food for thought. As IF 
            already operates successfully within the current financial system, 
            it should therefore not be a major leap of faith for institutions 
            and regulators alike, to incorporate some of the guiding principles 
            in IF like risk management and governance, for their own benefit and 
            the larger interest <br>
            of consumers. </font></p>
