				<p align="justify" style="margin-left: 10px; margin-right: 10px"><b><font face="Verdana" size="4">
				<br>
				</font></b><b><font face="Verdana" size="4">Employees cannot 
                follow if executives do not lead</font></b><font face="Verdana" size="1"><br>
                <b>Robert S Kaplan, the founder of the Balanced Scorecard 
                method, speaks exclusively to OER about the utility of the 
                strategy as a management tool for better planning and execution<br>
                </b><br>
                <img border="0" src="event1.jpg" align="left" hspace="8" vspace="6" width="300" height="222">Robert S Kaplan is Baker Foundation professor at the Harvard 
                Business School (HBS). Kaplan joined the HBS faculty in 1984 
                after spending 16 years on the faculty of the business school at 
                Carnegie-Mellon University, where he served as a Dean from 1977 
                to 1983. He has been a co-developer of both activity-based 
                costing and the Balanced Scorecard (BSC). He has authored or 
                co-authored 14 books, 17 Harvard Business Review articles, and 
                more than 120 other papers. Kaplan will be speaking at the 
                Balanced Scorecard Forum 2009, which takes place at JW Marriott 
                Hotel, Dubai on 28 March- 2 April 2008. He will focus on how to 
                achieve stability by linking strategy to operations.<br>
                <br>
                <b>Can a good strategy beat the current global financial crisis?</b><br>
                It can. Many of the failed or failing organisations worldwide 
                suffered from having too narrow a focus on short-term financial 
                performance caring only about quarterly and annual “profits” and 
                how to make as much money as soon as possible, without regard to 
                future consequences. This is exactly the problem that the BSC 
                was designed to mitigate. The BSC’s financial perspective 
                focuses on long-term shareholder value creation. This 
                perspective should include objectives and metrics for enterprise 
                risk management (ERM), helping to pre-empt the narrow pursuit of 
                short-term gains that may prove disastrous for the long-term 
                profitability of an enterprise. From my experience companies’ 
                strategies lacked risk management objectives, leading to 
                strategies that were not robust with respect to changes in the 
                economic and business environment.<br>
                <br>
                Risk, to state the obvious, is inherent in all business and 
                financial activity. Financial institutions in general have 
                metrics related to risk exposure, such as ‘value-at-risk,’ but 
                most financial institutions, and certainly the ones that failed, 
                treated risk management as a regulatory and compliance function, 
                and not as a strategic issue. Their local risk management 
                metrics met regulatory rules, but did not reveal the strategic 
                exposure that companies actually had since they were based on 
                invalid data.<br>
                <br>
                <img border="0" src="event.jpg" align="right" hspace="8" vspace="6" width="197" height="441"><b>Could the use of BSC have helped organisations to combat the 
                crisis?</b><br>
                Actually even with a BSC that included metrics of risk exposure, 
                the metrics – calculated on data that did not reflect a complete 
                cycle in the housing market – would have failed to reveal the 
                high risk that financial institutions had assumed by taking 
                highly leveraged positions in mortgage securities.<br>
                <br>
                However, if the executives were able to aggregate the risk 
                exposure onto the company’s BSC and stimulate discussion at 
                monthly strategy review meetings, BS C could have led executives 
                to decide to reduce certain asset positions. It can certainly 
                decrease the leverage they had taken against those asset 
                positions, thereby allowing the companies to survive better when 
                the adverse events, a decline in housing values, did occur.<br>
                <br>
                <b>What particular issues around strategy execution need to be 
                better addressed in business? What is the role of leadership in 
                such matters?</b><br>
                There are two key issues. First is leadership. Without strong 
                visionary leadership, no strategy will be executed effectively. 
                I now believe that strong leadership is both necessary and 
                sufficient for successful strategy execution with strategy maps 
                and Balanced Scorecards. The second key issue is to recognise 
                that strategy and operations (or tactics) are both important but 
                they are different. The normal course of events is for companies 
                to focus on day-to-day operations, short-term problem solving, 
                and how to meet the quarterly earnings target.<br>
                <br>
                Management meetings focus on fighting fires and fixing problems. 
                Often little time and few resources get committed to strategic 
                issues. We do not advocate abandoning an intense focus on 
                operations and their improvement. However, we do advocate that 
                reviewing strategy, not just describing it is important, and 
                that the senior management team needs to have regular, probably 
                monthly, meetings that focus only on strategy. “Strategy without 
                tactics is the long road to victory; Tactics without strategy is 
                the noise before defeat.” This quote highlights the importance 
                of integrating strategy and operations, a central theme <br>
                in our strategy execution system. <br>
                <br>
                <b>An important part of BSC is the ability to link cost and 
                performance management to strategy implementation and 
                operational excellence. Sometimes, though performance can be 
                something intangible, how can you measure that? </b><br>
                We believe that measurement is fundamental for management and 
                learning. If you do not measure something, you cannot manage and 
                improve it. That is why it is now so important for companies to 
                develop valid measures of their risk exposure onto their 
                strategy scorecards. The purpose for the Balanced Scorecard is 
                to measure many important factors that are neglected by 
                financial statements. Virtually anything can be measured, but 
                usually not in financial terms.<br>
                <br>
                Customer relationships, the customer value proposition, quality, 
                innovation, employee motivation, employee capabilities, and 
                organisational culture can all be measured, and, hence, managed 
                and improved. Some of the measurements can come from surveys and 
                interviews. We can ask customers to rank, on a 1-10 scale, how 
                likely they are to recommend the company’s product or service to 
                a friend, neighbour or colleague. Reputation for example can 
                certainly be measured.<br>
                <br>
                Each year, Fortune magazine devotes an issue to “The Most 
                Admired Corporations.” Do you think they make up these ratings? 
                No, they do a survey of hundreds or thousands of individuals to 
                learn about the reputation of companies from all over the world. 
                So a principal contribution for the Balanced Scorecard is to 
                provide a robust measurement and management framework for all of 
                the intangible assets critically important for successful and 
                sustainable strategy execution. This could not be done when 
                companies use only financial reports as their primary management 
                system.<br>
                <br>
                <b>What role does human resources play in a business strategy? 
                How does a company make sure that its employees are committed?</b><br>
                Ultimately, effective strategy execution requires that employees 
                be personally committed to helping their enterprise and unite to 
                achieve strategic objectives. The process to align employees 
                with the strategy requires three steps: communicate and educate 
                employees about the strategy, link employees’ personal 
                objectives and incentives to the strategy, and align personal 
                training and development programmes to provide employees with 
                the knowledge, skills, and competencies they need to help 
                implement the strategy. Communication by leaders is also 
                critical. Employees cannot follow if executives do not lead.<br>
                <br>
                Employees want to take pride in the organisation in which they 
                spend much of their waking lives. They should understand how the 
                success of their organisation creates benefits not only to 
                shareholders but also to customers, suppliers, and the 
                communities in which it operates. Employees should feel that 
                their organisation functions both efficiently and effectively.<br>
                <br>
                No one enjoys working for a failing, underperforming enterprise. 
                Employees should be reassured that the organisation does not 
                squander resources in pursuit of its mission. Poorly functioning 
                organisations, bureaucracies that hamper decision-making, and 
                turf battles arising from the narrow-mindedness often spawned by 
                functional silos are visible to everyone and demoralising to 
                all. <br>
                <br>
                <b>How much value does a successful strategy execution add to a 
                company?</b><br>
                You cannot really attribute the performance improvements that an 
                enterprise experiences to any single factor or set of factors.<br>
                <br>
                Generally, when a company succeeds, many forces are working 
                together: great leadership, a good strategy that is well 
                described, understood and implemented, motivated managers and 
                employees, great operating performance aligned to the strategy, 
                and continued attention to fine-tuning and doing mid-course 
                adaptations to the strategy and its execution.<br>
                <br>
                All are required for success; if any component is missing, the 
                strategy will likely fail to be executed effectively. Because of 
                the multiplicative nature of the factors that contribute to 
                success, the omission of any one could cause the strategy to 
                fail.<br>
                <br>
                That is why we cannot measure well the exact contribution of the 
                scorecard to company success. We do look, however, to the CEO 
                and senior executive team who, using their subjective judgment, 
                will generally say that the scorecard implementation played a 
                significant role in success and that the great performance could 
                not have been achieved without it. </font></p>
