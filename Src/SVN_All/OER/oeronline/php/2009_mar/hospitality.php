            <font size="4" face="Verdana, Arial, Helvetica, sans-serif">
            Unmistakable charm</font></b><font size="1" face="Verdana, Arial, Helvetica, sans-serif"><br>
            <b>No stranger to Oman, Christoph K Franzen, general manager, Grand 
            Hyatt, Muscat is looking forward to his second outing in the 
            Sultanate. He speaks to Mayank Singh in his first media interview</b><br>
            <br>
            <img border="0" src="hosp1.jpg" align="left" hspace="8" vspace="6" width="250" height="304">Coming from a family which had a hospitality business, Christoph K 
            Franzen, general manager, Grand Hyatt Muscat grew up with a 
            first-hand feel of the industry. “My parents used to own a hotel in 
            Zermatt-Matterhorn, the ski and mountain climbing resort in 
            Switzerland,” says Franzen. A picture of Zermatt adorns the walls of 
            his well appointed office. Coming from such a background it comes as 
            no surprise that he went onto to join the hotel business. A chef 
            apprenticeship in 1986, followed by a course from a hotel school in 
            Switzerland set him on to a career in the industry. Over the years 
            he has worked for the Hilton group and the Hyatt chain in places 
            like Dubai, Moscow, US and Australia.<br>
            <br>
            Franzen worked with the Grand Hyatt, Muscat from 2001 onwards for 
            four years. A homecoming of sorts, he speaks eloquently about the 
            charm of the property, “If you contrast this with the hotels in 
            Dubai, this is unique and exudes a certain warmth.” Its déjà vu time 
            in more ways than one. Franzen was posted to Oman a day after the 
            twin tower bombings in the US on September 11, 2001. The hospitality 
            business took a nosedive in the aftermath of the attacks and the 
            current economic meltdown poses a similar challenge.<br>
            <br>
            <b>Facing the challenge</b><br>
            In an environment wherein companies are cutting costs and driving 
            tougher bargains, hotels face the challenge of maintaining their 
            profitability without compromising on the comfort level accorded to 
            guests or their service standards. “The market is shifting from a 
            sellers market to a buyers market and hotels have to get used to 
            negotiating in a softer market, with a more lenient approach,” says 
            Franzen. Tougher times means that hotels need to offer special 
            deals, upgrade services etc to attract guests. <br>
            <br>
            Thanks to its positioning (as a luxury hotel) and its location – in 
            the diplomatic area, Hyatt attracts a fair share of business 
            travellers. And though the downturn is impacting occupancies, the 
            hotel is better placed than most of its competitors. Says Franzen, 
            “We have several advantages like the Hyatt international sales 
            structure, our reputation as a premium hotel and a chain of 164 
            hotels which acts as a strong brand ambassador.” At home the hotel 
            is working out attractive packages for guests in conjunction with 
            tour operators and airlines. For example, it has given Oman Air, the 
            Sultanate’s official carrier a set of preferential dates. On these 
            dates, guests get better rates or enhanced offerings from the hotel 
            and the airline. Adds Franzen, “We are not lowering our rates but 
            adding benefits like late check outs, early check in, offering a 
            higher room category for the same fare etc.”<br>
            <br>
            Though most players are walking that extra yard to attract business 
            travellers, industry watchers feel that there are certain 
            fundamental challenges that need to be addressed. First, Oman is 
            perceived as an expensive market by travellers. A back of the 
            envelope calculation shows that while a five star room night in 
            other markets costs $125 on an average, in the Sultanate it shoots 
            upto $200-250. The weakening of the pound (and Euro) against the 
            dollar, means that European travellers coming to Oman have to shell 
            out more, even after factorising the discounts on offer. <br>
            <br>
            There are others who complain about the lack of an adequate number 
            of airlifts to Oman. “You can open more hotels, but you need more 
            connectivity during high compression periods. I believe that Oman 
            Air is aware of this and that initiatives like the airport expansion 
            and the growing network of carriers serving Oman are steps in 
            addressing these concerns,” says Franzen. <br>
            <br>
            <b>Bottlenecks galore</b><br>
            <img border="0" src="hosp.jpg" align="left" hspace="8" vspace="6" width="192" height="206">Different weekends between UAE and Oman makes it difficult for 
            hotels in Oman to attract people from its neighbouring country. Oman 
            follows a Thursday-Friday weekend, while UAE has moved to a 
            Friday-Saturday schedule. Since there is a dip in occupancies from 
            the business traveller, hotels try to make up for this by offering 
            weekend packages for the leisure segment. <br>
            <br>
            Different weekends mean that while for a UAE guest a weekend begins 
            on Friday, for hotels in Oman the weekend ends that day. Hotels are 
            not too keen to go that extra yard as the week starts as business 
            executives start coming in. Franzen singles out the immigration 
            process as another bottleneck, “A number of people tell us that the 
            immigration process needs to fasten up.” He pooh-pooh’s charges 
            about a lack of hotel rooms in Oman another pet peev of critics, “I 
            have not heard of any shortages of rooms in Oman. The tourism 
            ministry has been participating in trade shows regularly and they 
            have big plans for promoting the country.”<br>
            <br>
            Ask him about the best part of working in the industry and he says, 
            “You work in a very nice environment and get to meet a lot of famous 
            people.” Franzen has rubbed shoulders with The likes of John 
            Travolta, General Tommy Franks, Mike Tyson and numerous heads of 
            state. The flip side is “the long hours that everyone in a hotel 
            puts in.”<br>
            <br>
            An avid motor biker Franzen is looking at driving around Oman on his 
            BMW bike. Though he started biking only a year and a half ago, he 
            has taken to biking with a vengeance. “I was in Dubai (Hyatt) and 
            Josef Miklave, the executive chef, a colleague of mine used to 
            constantly talk about biking, so one day I went ahead and bought two 
            bikes one for him and one for myself and asked him to pay up for his 
            bike.” He speaks passionately about his rendezvous to Musandam and 
            Hatta pools. “Biking gives you peace and freedom,” he says. Franzen 
            could not have wished for a better terrain to indulge in his passion 
            than Oman. </font></p>

