                <b>The book is a genuine effort at laying bare the author’s 
                political, social and economic convictions. While one may 
                disagree with him, the sincerity of his purpose cannot be 
                questioned</b><br>
                <br>
                <img border="0" src="browsing.jpg" align="left" hspace="8" vspace="6" width="200" height="381">The phrase, “The Audacity of Hope” — comes from Barack Obama’s 
                2004 Democratic Convention keynote address, which made him the 
                party’s rising young hope. The eponymous book is much more of a 
                political document. Portions of the volume read like an election 
                speech, and the bulk of it is devoted to laying out Obama’s 
                policy positions on a host of issues, from education to health 
                care to the war in Iraq.<br>
                <br>
                <b>Personal touch</b><br>
                But while Obama occasionally slips into the flabby platitudes 
                favoured by politicians, enough of the narrative voice in this 
                volume is recognisably similar to the one in Dreams From My 
                Father, an elastic, personable voice that is capable of 
                accommodating everything from dense discussions of foreign 
                policy to streetwise reminiscences, incisive comments on 
                constitutional law to New-age personal asides. <br>
                <br>
                One gets the feeling that Obama has not reinvented himself as he 
                has moved from job to job (community organiser in Chicago, 
                editor of The Harvard Law Review, professor of constitutional 
                law, civil rights lawyer, state senator) but has instead 
                internalised all those roles, embracing rather than shrugging 
                off whatever contradictions they might have produced.<br>
                <br>
                The word authenticity is usually used to describe Obama, 
                pointing to his ability to come across to voters as a regular 
                person and not a pre-packaged politician. And in these pages he 
                often speaks to the reader as if he were an old friend. He 
                peppers policy recommendations with colourful asides about the 
                absurdities of political life. <br>
                <br>
                <b>Interesting anecdotes<br>
                </b>He recalls a meet-and-greet encounter at the White House 
                with George W. Bush, who warmly shook his hand, then “turned to 
                an aide nearby, who squirted a big dollop of hand sanitizer in 
                the president’s hand.” (“Good stuff,” he quotes the president as 
                saying, as he offered his guest some. “Keeps you from getting 
                colds.”) And he recounts a trip he took through Illinois with an 
                aide, who scolded him for asking for Dijon mustard at a T.G.I. 
                Friday’s, worried the senator would come across as an elitist; 
                the confused waitress, he adds, simply said: “We got Dijon if 
                you want it.”<br>
                <br>
                His thoughts on domestic and foreign policy try to hover around 
                consensus-building. Some of his recommendations devolve into 
                little more than fuzzy statements of the obvious: i.e., that 
                America’s “addiction to oil” is affecting the economy The book 
                is a must read for anyone interested in the future direction 
                that the US is going to take. </font></p></td>
