            no doubt provide the strong backbone for the company’s growth plan 
            in 2009 and take ProCurve into new markets. Their new data centre 
            switches and management software is geared to help customers lower 
            costs, mitigate risk and speed business growth. The HP ProCurve 
            Network Ecosystem (HP ProCurve ONE) uses the company’s open networks 
            platform as its base, allowing customers to integrate network 
            applications and services from well known alliance partners such as 
            Microsoft, Riverbed, McAfee, F5 and Avaya directly with HP ProCurve 
