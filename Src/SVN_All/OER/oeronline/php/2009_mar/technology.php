            into the large enterprise arena where previously it was mainly 
            focused on the small to medium enterprise segment. The merger gives 
            solution providers access to HP’s rapidly growing networking 
            equipment group and enables them to sell ProCurve as part of an 
            end-to-end solution.<br>
            <br>
            This move, along with the recent launch in January of ProCurve’s 
