                </font><b><font face="Verdana" size="4">Pros at Networking</font></b><font face="Verdana" size="1"><br>
            <b>Despite the global economic downturn HP ProCurve is forecasting 
            continued growth for the year ahead. Jessica Brookes reports from HP 
            ProCurve’s European News Briefing held at Chantilly, France<br>
            </b><br>
            Alaa AlShimy, general manager of ProCurve Networking for the Middle 
            East speaks about the company’s plans for growth in 2009. Excerpts 
            from the interview:<br>
            <br>
            <b>
            <img border="0" src="tech.jpg" align="left" hspace="8" vspace="6" width="250" height="320">What is your strategy for growth in the Middle East region?<br>
            </b>The Middle East compared to other emerging markets such as Russia 
            and Eastern Europe is in a great position. We experienced large 
            growth in the Middle East last year, almost 100 per cent 
            year-on-year and we are expecting a growth of 40 per cent in the 
            region this year.<br>
            <br>
            <b>Despite the economic slowdown do you still expect to grow?<br>
            </b>Despite the negative impact the financial crisis is having across 
            the world, the downturn is presenting an opportunity for a number of 
            reasons. The first one is that with the current economic situation 
            customers are asking for more economic value at less money, and this 
            is something that ProCurve offers. We give a lifetime warranty 
            whereas our competitors sell it at a percentage of the product value 
            and we also provide free software support over phone and online. The 
            products we offer are very competitive in comparision to other 
            options available in the market. So in the current situation, with 
            customers asking for more value at less money, people are turning to 
            ProCurve. The second reason is that we previously had not taken 
            aggressive steps in the Middle East. But now we have hired qualified 
            people and we are going to hire more. Our market share today is only 
            8 per cent; we are aiming for much higher than that so we have a 
            very big opportunity for growth.<br>
            <br>
            The third reason is the integration with HP’s Technical Solutions 
            Group (TSG). This means that we can address all our enterprise 
            customers with end-to-end solutions. We can deliver integrated 
            solutions with our storage partners, our ProCurve ONE alliance 
            partners, our software partners and our services organisations. 
            Previously our worldwide sales force was only 1,000 people but now 
            with the TSG integration we have 23,000 people. I see more impact in 
            some countries than others in the Middle East. So, of course, we are 
            going to fine tune our focus on the countries with less impact and 
            that is mainly the GCC. But we will maintain focus and operations 
            for other countries.<br>
            <br>
            <b>
            <img border="0" src="tech2.jpg" align="right" hspace="8" vspace="6" width="195" height="152">How does your presence in Oman compare to its neighbouring 
            countries?<br>
            </b>If I compare Oman to Dubai we don’t have the same coverage. Dubai 
            actually is the hub for us and it is the headquarters for HP in the 
            Middle East. The largest number of resources for ProCurve is based 
            in Dubai. However, we have started to focus more on Oman now. In 
            particular, in the last four months we have increased our presence 
            with a dedicated sales and technical team. We also have a legal 
            entity for HP in Oman. We have had a couple of very good engagements 
            and we are planning to come up with some good announcements for Oman 
            in 2009.<br>
            <br>
            <b>ProCurve is marketing itself as a low cost product provider. How do 
            you achieve this goal without compromising on quality?</b><br>
            ProCurve started in HP 30 years back. With investment in R&amp;D we were 
            able to design our own chip sets. When you open any electronic 
            equipment you will find a number of chips inside. If you compare 
            ProCurve switches to the leading competitors you will find only one 
            chip versus maybe eight to ten chips in the competition’s switches. 
            It means that for every chip you need to pay for in a ProCurve 
            switch, you need to pay eight times for other switches on an average 
            – it means that the competitor’s products consume more power, cost 
            more and have a higher probability of failure. So we manage our 
            costs in designing and producing flexible products of reduced 
            complexity. That is how we can offer lower price products that don’t 
            compromise quality. </font></p>

