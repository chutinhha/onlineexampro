            <p align="justify" dir="ltr" style="margin-left: 5; margin-right: 5"><font face="Verdana" size="1">
            <b>Sovereign defaults</b><br>
            Where are the sovereign defaults that are in more dire financial 
            shape than in 1997? The Baltics, Pakistan, Vietnam, the Gulf and 
            Southeast Asia prove that construction booms driven by offshore 
            borrowing inevitably end in property crashes, corporate default, 
            bank funding crises, capital flight and high inflation. So, global 
            banks bet on Eastern Europe as a play on EU convergence. Governments 
            and entire banking systems have no access to Fed swap lines or IMF. 
            It is possible that gold prices will rise to $1100 to $1250 in the 
            next 12 months. <br>
            <br>
            The Chinese Yuan-dollar cross rate is arguably the most politically 
            sensitive strategic equation in global finance. Even though Geithner 
            lambasted China as a currency manipulator and Premier Wen Jiabao 
            railed against US protectionism at Davos, it is unthinkable that 
            Beijing will provoke a trade war with a devaluation of the Yuan. In 
            fact, the aggressive easing by the People’s Bank of China and the 
            $580bn stimulus could well cushion the plunge in export and even 
            trigger a strengthening of the Yuan. While NDF forwards suggest that 
            the currency markets believe the Chinese Yuan will depreciate to 
            7.0, I believe we could well rise to 6.60. In the short term, I 
            believe the Shanghai stock market is clearly overbought and GDP 
            growth will fall in the Middle Kingdom, the world’s second largest 
            economy. However, Chinese equities remain one of the few bright 
            spots in the emerging markets, with some fund manager flows coming 
            in to Mainland cement railroad and steel companies that will benefit 
            from the Communist Party’s infrastructure programme. The Chinese 
            Yuan could thus strengthen from the fund inflows. <br>
            <br>
            I am skeptical if the Indian Rupee can withstand a new wave of 
            global risk aversion and the politics of an election that could well 
            witness a BJP win over the ruling Congress. Bank credit, capex 
            freeze and a property crash all suggest that Indian GDP growth will 
            fall to 3 per cent, with additional FII outflows from sensex once 
            corporate earnings disappoint. The RBI will lower its repo rate to 4 
            per cent and I envisage the Indian Rupee at 52-54 against the 
            dollar.
            </font></p>
