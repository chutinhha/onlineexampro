            </b><b><font face="Verdana" size="4">MAKING MONEY IN GLOBAL 
            CURRENCIES</font></b><font face="Verdana" size="1"><br>
            <b>Gold is clearly the safe haven in a world of financial turmoil 
            even though inflation expectations and crude oil have fallen since 
            their July 2008 highs. It is no coincidence that gold has broken 
            free from its traditional inverse correlation with the dollar since 
            the failure of Lehman Brothers and benefits from its increasing role 
            as a reserve asset </b></font></p>
            <p align="justify" dir="ltr" style="margin-left: 5; margin-right: 5"><font face="Verdana" size="1"><b>
			<font color="#CC3333">By Matein Khalid</font></b><br>
            <br>
            <img border="0" src="peri.jpg" align="right" hspace="8" vspace="6" width="125" height="148">The Australian dollar lost more than a fifth of its value in 2008 
            because commodities prices plunged and the Japanese yen finance 
            carry trade was unwound as the Reserve Bank slashed the benchmark 
            Aussie money market rate from 7 per cent to 3.25 per cent. The 
            Australian dollar is vulnerable to falls in the price of iron ore, 
            coal, gas, gold and platinum as commodities account for an 
            incredible 60 per cent of total exports. Moreover, the Aussie/Yen 
            cross trade is a classic metric of risk aversion, as Japanese 
            capital flows to high yield money market instruments freeze when 
            currency risk rises. However, the Australian dollar has largely 
            priced in the RBA interest rate cuts and the end of irrational 
            exuberance in the commodities market.<br>
            <br>
            <b>Currency woes </b><br>
            I doubt if the Japanese yen, which has risen since 120 at the outset 
            of the currency crises, to 90 now, can rise much more than 87-88. 
            After all, the Bank of Japan has signalled that it could well 
            intervene to restrain further yen appreciation because Japan’s 
            economy contracted at a traumatic 14 per cent in 4Q2008, its exports 
            colossi such as Sony, Toyota, Hitachi and Toshiba face the first 
            operating losses in decades and the LDG, with an unpopular Prime 
            Minister Taro Aso, does not want to face the electorate amid a 
            recession. Another embryonic but positive trend for the Aussie 
            dollar is the rapid expansion in the Chinese money supply and 
            Beijing’s $580bn fiscal stimulus. This trend could well put a bid on 
            commodities, as the recent 150 per cent rise in the Baltic Dry 
            Freight shipping index definitely suggests. I admit, that neither of 
            these trends are remotely consensus in the foreign exchange market 
            now.<br>
            <br>
            The Japanese yen is well bid because of the bear market in global 
            equities and Wall Street’s skepticism about Treasury Secretary 
            Geithner’s bank bailout plans. Dr Copper and industrial metals are 
            still trapped in vicious bear markets as Chinese export volumes 
            plummet. However if the yen weakens and Chinese demand for iron ore 
            rises, the Aussie dollar could soar from its current 65 cents to as 
            high as 75 cents against the greenback and at least 80 yen.<br>
            <br>
            <b>Precious commodity</b><br>
            Gold is clearly the safe haven in a world of financial turmoil even 
            though inflation expectations and crude oil have fallen since their 
            July 2008 highs. I find it no coincidence that gold has broken free 
            from its traditional inverse correlation with the dollar since the 
            failure of Lehman Brothers and benefits from its increasing role as 
            a reserve asset in a world where the central bank printing presses 
            compromise the future of the dollar, Euro and sterling.<br>
            <br>
            Gold is now more correlated with mood swings in risk appetite and 
            volatility in global equities. With short term dollar rates at zero 
            and the rise in sovereign default spreads for US Treasury debt 
            (quoted in Euros, not dollar, for obvious reasons!), gold has become 
            a de facto currency that is decoupled from oil, copper and even 
            silver. International banking risk will only rise as cross-border 
            capital flows to the emerging markets plummet, with even the IMF 
            unable to prevent a cascade of sovereign defaults. It is also 
            ominous that inflows into gold coins, ETF, bars and bullions have 
            only risen even while jewellery demand in India falls.
            </font></p>
            <p align="center" dir="ltr" style="margin-left: 5; margin-right: 5">
