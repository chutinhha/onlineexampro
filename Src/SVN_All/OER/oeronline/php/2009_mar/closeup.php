                <p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1">
                <br>
                </font><b><font face="Verdana" size="4">Qatar: Gas sector aids 
                economic prospects<br>
                </font></b><font face="Verdana" size="1">
                <b>International Monetary Fund expects growth of Qatar’s GDP in 
                2009 to remain in line with that of 2008. The confidence stems 
                from fresh gas and petrochemical projects coming on stream as 
                Continued progress made in the gas industry is expected to 
                compensate for a drop in oil prices<br>
&nbsp;</b></font></p>
                <p align="justify" style="margin: 3px 10px"><b>
				<font face="Verdana" size="1" color="#CC3333">By Dr Jasim Husain 
				Ali</font></b><font face="Verdana" size="1"><br>
                <br>
                </font><b><font face="Verdana" size="4">
                <img border="0" src="close.jpg" align="left" hspace="8" vspace="6" width="125" height="148"></font></b><font face="Verdana" size="1">The 
                International Monetary Fund (IMF) is optimistic about Qatar’s 
                economy in 2009, and for good reasons. The positive outlook 
                partly reflects on-going successes made in the country’s energy 
                sector in general and gas industry in particular. Now, Qatar is 
                the unchallenged global exporter of liquefied natural gas (LNG). 
                Qatar produces some 38mn tonnes of LNG yearly but is expected to 
                reach 77mn tonnes a year by 2012.<br>
                <br>
                In retrospect, Qatar managed to develop its gas industry to 
                extraordinary levels through joint production agreements with 
                international oil firms. Sheikh Hamad bin Khalifa Al Thani 
                opened the country’s energy sector to international oil firms 
                ever since assuming power in his country in the mid 1990s.<br>
                <br>
                <b>Report card</b><br>
                To be sure, Qatar has put on display an impressive performance 
                in 2008. The IV economy review put growth of the country’s gross 
                domestic product (GDP) at 16 per cent in 2008. Ostensibly, the 
                figure is in real terms adjusted for inflation. Yet, Qatar 
                suffered from a high inflation rate of 15 per cent last year. 
                Altogether, Qatar’s economy grew by a remarkable 29 per cent in 
                nominal terms in 2008. Undoubtedly, the growth level is 
                exceptionally high in normal conditions, let alone the difficult 
                circumstances encountered in the second half of 2008, namely 
                emergence of the financial crisis.<br>
                <br>
                Still, the IMF expects growth of Qatar’s GDP in 2009 to remain 
                in line with that of 2008. The development rests on fresh gas 
                and petrochemical projects coming on stream. In other words, 
                continued progress made in the gas industry should compensate 
                part of drop in oil prices. Qatar boasts oil production capacity 
                of some 800,000 bpd. Likewise, the IMF seems to be happy with 
                the policy of linking the Qatari riyal to the US dollar. The 
                policy helps bringing about stability in a relatively small 
                economy like that of Qatar. According to the Economist 
                Intelligence Unit, Qatar’s GDP amounts to $71bn. The figure 
                makes up less than one per cent to the size of US’s GDP of 
                $13trn.<br>
                <br>
                <img border="0" src="closeup.jpg" align="right" hspace="8" vspace="6" width="236" height="508">Small investors, who are the majority, tend to seek stability. 
                In other words, the link allows Qatar the opportunity to avoid 
                unnecessary volatility associated with floating currencies. In 
                addition, the link uniquely fits the nature of Qatari exports 
                notably oil and gas, which are priced in the US dollar.<br>
                <br>
                <b>Declining inflation</b><br>
                Additionally, the forecast calls for lower inflation rate on the 
                back of declining economic growth rates worldwide in the 
                aftermath of the financial crunch. Reflecting lower demand, 
                slower global economic growth should lead to a drop in prices. 
                Qatar imports most of its needs from abroad. <br>
                <br>
                According to official sources, inflation rate in Qatar stood at 
                13.16 per cent in the last quarter of 2008 partly reflecting 
                lower rental and energy rates. The figure compares favourably 
                with 18.9 per cent and 42.2 per cent in the third and second 
                quarters of 2008, respectively. Still, the consumer price index 
                amounted to 180 points by end of 2008 versus 159 points a year 
                earlier. Clearly, containing inflation remains a challenging 
                task for Qatari authorities. <br>
                <br>
                <b>Looking ahead</b><br>
                On the other hand, the IMF has warned of fundamental challenges 
                encountering the Qatari economy. Says the IMF report, “The key 
                challenges facing the authorities are to lower Qatar’s high rate 
                of inflation, continue to shield the economy from the global 
                financial crisis, ensure that rapid credit growth does not 
                undermine bank soundness, and diversify the economy.”<br>
                <br>
                True, the general trend calls for lower inflationary pressures 
                in the world in 2009, but the general price rise poses a threat 
                to Qatar’s economic well-being at large due to firm government 
                expenditures. To be sure, public sector spending has become only 
                more important after the global financial crisis.<br>
                <br>
                It is believed that the credit crunch has adversely affected 
                confidence amongst private sector investors, hence the need for 
                public sector investments.<br>
                <br>
                Qatar’s budget for fiscal year 2008-09 is expansionary in 
                nature. Total expenditures are up by 46 per cent over the 
                allocated amount for fiscal year 2007-08. In particular, 
                officials allocated a large chunk of funds for spending on 
                infrastructure projects notably expanding road network. Qatar’s 
                fiscal year commences at the start of April. Still, the 
                authorities suggest the upcoming budget for fiscal year 2009-10 
                would be the largest in the country’s history in terms of 
                projected spending. <br>
                <br>
                <b>Per capita income</b><br>
                Per capita income best depicts success of the country’s economic 
                choices. Qatar’s GDP per capita on purchasing power parity (PPP) 
                basis is the second highest in the world after Luxembourg. The 
                2008 Human Development Index (HDI) puts Qatar’s GDP per capita 
                on PPP basis at $72,969 per annum. The result is testimony of 
                some correct choices made by the authorities. Qatar advanced by 
                a single notch to number 35 globally of HDI’s 2008 report. The 
                report ranks countries on the basis of their performance on 
                three variables, namely life expectancy at birth, education and 
                income on PPP basis. <br>
                <br>
                In particular, Qatar stands the chance of further improving its 
                global ranking due to investments made on health care services 
                over the few years in connection with hosting international 
                sporting activities such as Asian Games in late 2008. The 
                authorities prepared medical facilities for the tournament but 
                transferred to general use after the games.<br>
                <br>
                Also, Qatar has committed resources for advancing education by 
                developing a purposely-designed area known as Educational City. 
                The project covers 14 square kilometers and provides facilities 
                for school age children to research level institutions. Numerous 
                American universities such as Carnegie Mellon and Texas A&amp;M have 
                a presence in this area.<br>
                <br>
                Nevertheless, officials must work hard to make the economy less 
                dependent on the hydrocarbons sector. The energy sector accounts 
                for more than two thirds of Qatar’s treasury income. The 
                extraordinary reliance on oil and gas puts the economy at the 
                mercy of external shocks. At the same time, the authorities 
                should spare no efforts to bring inflation under control, as 
                inflationary pressures tend to undermine a country’s well-being. </font></p>
