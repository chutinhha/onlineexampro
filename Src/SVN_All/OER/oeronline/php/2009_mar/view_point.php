		        <td valign="top" width="100%"> <p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
                    </font><b><font face="Verdana" size="4">Doing Business in 
                Egypt</font></b><font face="Verdana" size="1"><br>
                <b>Sitting at the crossroads linking Europe, the Middle East and 
                Africa, Egypt is one of the region’s most important political 
                power brokers and plays a leading role in commercial, financial, 
                cultural and diplomatic spheres<br>
&nbsp;</b></font><p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1">
                <b>
                <font color="#CC3300">By Sridhar Sridharan</font></b><br>
                <br>
                <img border="0" src="view.jpg" align="left" hspace="8" vspace="6" width="102" height="119">Egypt, 
                situated in the north-east corner of the African continent, is 
                bordered by the Mediterranean Sea to the north and the Red Sea 
                to the east. The country is bisected by the highly fertile Nile 
                valley, where most of the economic activity takes place. Cairo, 
                the capital of Egypt, is the largest city in Africa, the Arab 
                world and the Middle East. It is also the commercial and 
                political center of Egypt. Industrial development is centered in 
                Cairo, Alexandria (Egypt’s chief port) Ismailia, Port Said, 
                Luxor, Aswan and Assiut. The official language is Arabic, 
                although English and French are also widely used in business, 
                academic and political circles.<br>
                <br>
                <b>Economic Overview</b><br>
                Egypt sits at the pivotal crossroads linking Europe, the Middle 
                East and Africa in an increasingly significant global business 
                network. The Arab world’s largest country in terms of 
                population, Egypt is also one of the region’s most important 
                political power brokers. The country plays a leading role in 
                commercial, financial, cultural and diplomatic spheres.<br>
                <br>
                Over the past few years, Egypt has carried out far-reaching 
                economic reforms and a liberalisation programme, designed to 
                both open up its economy and strengthen its unique position as 
                an international business hub. As it moves from a 
                state-dominated to a free market economy, Egypt has been 
                attracting direct foreign investors at a dynamic rate, as well 
                as experiencing impressive growth in virtually all sectors and 
                offering rich business opportunities. The International Monetary 
                Fund (IMF) has rated Egypt as one of the world’s top countries 
                in terms of economic reform, a statement that is supported by a 
                25 per cent annual capital growth, 7-8 per cent annual GDP 
                growth and a total annual FDI input of over $11bn. <br>
                <br>
                <img border="0" src="view1.jpg" align="right" hspace="8" vspace="6" width="179" height="580">Recent economic success is attributable to the privatisation of 
                certain sector industries and reforms introduced by the 
                government to customs, income and taxation related laws. Egypt 
                is the world’s largest exporter of cotton and its textile 
                industry is large. Other industries include the production of 
                cement, iron and steel, chemicals, fertilisers, rubber products, 
                refined sugar, tobacco, canned foods, cottonseed oil, small 
                metal products, shoes, and furniture.<br>
                <br>
                <b>Investment law and incentives<br>
                </b>The Investment Guarantees and Incentives law, governs all 
                investment, including foreign investment. The law offers 
                preferential treatment to foster private domestic and foreign 
                investment in Egypt. <br>
                <br>
                Foreign investments in the petroleum exploration and production 
                sector are governed by individual concession agreements between 
                foreign companies and the Egyptian General Petroleum Corporation 
                (“EGPC”). Foreign investments are welcomed in many areas 
                including industry and mining, tourism related services, 
                aviation transportation, maritime transport, infrastructure 
                projects relating to water, power, telecommunication and 
                satellite plants and data transmission networks.<br>
                <br>
                A one-stop shop for investors has been established at the 
                General Authority for Investment and Free Zones (GAFI) to 
                facilitate and simplify approval, registration, licensing and 
                certification of new projects. <br>
                <br>
                The Investment Law offers a variety of incentives to foreign 
                investors, including the following:</font><ul>
                  <li><p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1">
                Machinery, assets, materials and spare parts required for 
                investment projects can be imported without a license.</font></li>
                  <li><p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1">
                Products from manufacturing projects are not subject to price 
                controls. Investment projects may own land and real estate for 
                their operations.</font></li>
                  <li><p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1">
                Nationalisation and confiscation are not permitted. In addition, 
                investment project assets may not be seized, except by the 
                courts.</font></li>
                  <li><p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1">
                A custom duty at a unified reduced rate of five per cent is 
                applicable to machines and equipment necessary to establish 
                investment projects.</font></li>
                </ul>
                <p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1">
                <b>Structure of business entities<br>
                </b>Business organisations in Egypt are generally in the form of 
                incorporated companies, partnerships and sole proprietorships. 
                Partnerships and sole proprietorships are normally used by 
                Egyptian traders.<br>
                <br>
                <b>I. Joint Stock Company<br>
                </b>The minimum number of shareholders is three natural or legal 
                persons. Shares may be transferred between the shareholders in 
                the case of a closed company and may be sold to other parties. 
                Joint stock companies are subject to Egyptian tax on their 
                worldwide income. Double tax relief is given by deduction unless 
                the applicable double tax treaty provides for tax credit relief. 
                The companies are required to distribute 10 per cent of their 
                distributable profit to the employees up to a maximum of the 
                total annual employees’ salaries.<br>
                <br>
                <b>II. Limited Liability Company</b><br>
                The maximum number of shareholders is restricted to 50 who can 
                either be individuals or legal entities. The shares can be 
                transferred but the existing shareholders may exercise 
                pre-emptive right. An LLC is subject to Egyptian tax on its 
                worldwide income. Double tax relief is given by deduction unless 
                the applicable double tax treaty provides for tax credit relief.<br>
                <br>
                <b>III. Foreign Branch:<br>
                </b>There is no minimum capital requirement to establish a foreign 
                branch. However, a bank certificate showing an overseas transfer 
                of $5,000 in the name of the proposed branch is required to be 
                submitted. Normally, it should take around three months from the 
                date of submission of application to establish a branch. The 
                branch may not employ foreigners in excess of 10 per cent of its 
                work force or pay them more than 20 per cent of the total 
                payroll. Foreign branches are allowed to deduct head office 
                overhead expenses up to seven per cent of the taxable net 
                profit. <br>
                <br>
                <b>Requirements for foreign nationals</b><br>
                To work in Egypt, a foreigner must obtain a work permit from the 
                Office of Manpower and Training in the relevant governorate, or 
                from the free-zone authority for projects located in free zones.<br>
                <br>
                <b>Corporate Income Tax<br>
                </b>Egyptian corporations are subject to corporate profits tax 
                on profits earned in Egypt and abroad. Foreign companies are 
                taxed only on profits earned in Egypt. The standard rate of 
                corporate profits tax is 20 per cent. However, oil prospecting 
                and production companies are taxed at 40.55 per cent. Dividends 
                received by residents from foreign sources are taxed at 20 per 
                cent.<br>
                <br>
                <b>Withholding taxes<br>
                </b>There is no withholding tax on dividends. Taxes on income 
                from moveable capital and salaries are withheld at source. 
                Amounts withheld are paid quarterly to the Tax Department. <br>
                <br>
                The withholding tax rates on some important income items are as 
                follows: </font>
                <ul>
                  <li><p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1">
                Professional services: 10-15 per cent</font></li>
                  <li><p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1">
                Commercial services: 3 per cent</font></li>
                  <li><p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1">
                Commission to commercial agents: 10 per cent</font></li>
                  <li><p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1">
                Erection/installation contracts: 1 per cent of each invoice 
                issued</font></li>
                </ul>
                <p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1">
                <b>Double Tax Agreement<br>
                </b>Oman and Egypt have signed a Double Tax Agreement but it has yet 
                to come into force. <br>
&nbsp;</font><p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1">
                <b>Social Security:</b><br>
                Social insurance contributions are levied only on Egyptian 
                nationals with full-time employment. Both employers and 
                employees are required to make social security contributions.<br>
                <br>
                <b>Exchange control and currency restrictions in Egypt<br>
                </b>As a result of economic reforms and agreements reached with 
                the International Monetary Fund (IMF), exchange controls have 
                been cancelled in Egypt. The Foreign Exchange Law allows 
                individuals and legal entities to retain and transfer foreign 
                exchange in Egypt and abroad. <br>
                <br>
                The Government acknowledges that the free market guides the 
                rates of exchange set by the Central Bank of Egypt and other 
                approved banks and dealers. However, the Central Bank actively 
                monitors the exchange rate in order to assure the Egyptian 
                pound’s stability. Egypt eliminated foreign exchange transfer 
                restrictions in 1991. <br>
                <br>
                The Foreign Currency Law further relaxed the restrictions on 
                capital transfers and emphasised the right of individuals and 
                companies to transfer foreign exchange out of Egypt. Egyptian 
                law allows individuals and businesses to conduct all normal 
                foreign exchange transactions, including establishing foreign 
                exchange accounts and transferring foreign exchange in and out 
                of Egypt. Authorised banks may provide the full range of foreign 
                exchange transactions, including accepting deposits, executing 
                transfers and opening letters of credit.<br>
                <br>
                <b>Totalisation Agreements</b><br>
                To provide relief from payment of social security taxes in the 
                home country as well as in Egypt, Egypt has concluded agreements 
                with Cyprus, Greece and Sudan. These agreements are valid for an 
                unlimited <br>
                duration.<br>
                <br>
                <b>Import restrictions</b><br>
                The government imposes import controls to improve Egypt’s 
                balance-of-payments. Importers must obtain an approval to open a 
                letter of credit. A cash deposit ranging from 15 per cent to 100 
                per cent is also required, depending on the type of goods 
                imported. The origin of goods must be certified to enter Egypt.<br>
                <br>
                <b>Summary </b><br>
                As an emerging economy, Egypt provides many opportunities to 
                investors and exporters. It has carried out far-reaching 
                economic reforms designed to sustain investment and growth. It 
                has been experiencing impressive growth in virtually all sectors 
                and offers rich business opportunities to both locals and 
                foreigners. <br>
