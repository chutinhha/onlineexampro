            <br>
            <img border="0" src="board.jpg" align="left" hspace="8" vspace="6" width="250" height="253">As one steps into the Muscat Pharmacy head office in Wadi Kabir, the 
            first thing that catches one’s eye is the all round greenery – rows 
            of money plant creepers run all across the ceiling and various 
            plants stand out at corners. Enter the office of Nilesh Mehta, 
            administration manager, Muscat Pharmacy and one finds him evaluating 
            recently mounted frames on a series of paintings. “I have a passion 
            for beautiful things like arts, culture, plants etc,” he says. Given 
            this penchant, it is little surprise, that he is the driving force 
            behind the eco-friendly drive at the head office and the renovation 
            of Muscat Pharmacy outlets across Oman. “We wanted to create an 
            ambience wherein an ailing person starts feeling better the moment 
            he steps into the pharmacy.” In the last eight years, the company 
            has carried out renovation of 40 of its 65 outlets. <br>
            <br>
            <b>Family values<br>
            </b>A hands on manager, he believes that a leader needs to work hard 
            to set the right example. An ability to take quick decisions and 
            clarity are other attributes that come in handy. “If you are clear 
            then the staff know what is expected of them. Decision making is 
            important as it keeps the wheels turning without a hitch.” Having 
            joined the family business at the age of 16, in 1975, he learnt the 
            family values from his brothers: B S Mehta and D S Mehta. “The three 
            most important things for us are hard work, honesty and 
            transparency,” says Mehta.<br>
            <br>
            <b>Leisure pursuits<br>
            </b>
            <img border="0" src="board1.jpg" align="right" hspace="8" vspace="6" width="198" height="135">A nature lover, Mehta can be caught camping at various places 
            like Wadi Bani Khalid, Jebel Akhdar, Jebel Shams on weekends. 
            Spending evenings in the company of friends is another favourite. “I 
            love nature and dining out with friends.” An avid tennis player, he 
            had to give up the sport a few years back because of a wrist injury. 
            As a music lover he enjoys listening to old Hindi film songs and 
            ghazals. Says Mehta, “Old songs were meaningful and soulful, they 
            were closer to reality and had a certain romance. Contemporary 
            numbers are popular but transient in appeal.” He counts Jagjit 
            Singh, the Indian ghazal singer as his favourite singer. Coming to 
            movies he is partial to comedies. So while he may watch the 
            occasional Slumdog Millionaire, most of the time he relishes light 
            hearted stuff. Television viewing is limited to National Geographic 
            and Discovery channels. <br>
            <br>
            In times, when incidents of sibling rivalry have been hitting the 
            headlines, the Mehtas remain a close-knit family. Says Mehta, “There 
            is a lot of understanding and respect for each other. We have our 
            arguments, but all of us know where to draw the line.” Though a 
            family business he has left choice, of joining the business or doing 
            something on their own, to his children, “What they want to do is 
            entirely upto them.” </font></p>
