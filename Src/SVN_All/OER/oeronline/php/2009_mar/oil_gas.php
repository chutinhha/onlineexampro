		        <td valign="top" width="100%"> <p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
                    </font><b><font face="Verdana" size="4">On the rise</font></b><font face="Verdana" size="1"><br>
                <b>Oman is aiming to increase its oil and condensates’ 
                production in 2009. OER reports<br>
                </b><br>
                <img border="0" src="oil.jpg" align="right" hspace="8" vspace="6" width="176" height="131">Despite global financial meltdown and lower oil prices in the 
                international market, Oman is upbeat about its oil and gas 
                sector. Contrary to expectations, the government has reaffirmed 
                its commitment towards going ahead with capital investments in 
                projects that are in various stages of implementation. More 
                blocks are going to be up for grabs soon and the oil and 
                condensates’ production is also expected to increase this year.
                <br>
                <br>
                HE Nasser bin Khamis al Jashmi, Undersecretary, Ministry of Oil 
                &amp; Gas recently stated: “The oil and gas sector is going ahead 
                with executing the projects that have been committed earlier. 
                The ministry is planning to offer five new concession areas this 
                year. The ministry had recently awarded the Canadian Epsilon 
                Energy a concession for oil and gas exploration in block 55 in 
                the Wusta region. The Sultanate’s crude oil and condensates’ 
                production has risen from 710,000 bpd in 2007 to 757,000 bpd in 
                2008 and it is planned to increase the daily production this 
                year to 805,000 bpd.”</font><p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
                <img border="0" src="oil1.jpg" align="left" hspace="8" vspace="6" width="183" height="314"><b>PDO – leading from the front</b><br>
                Bringing more cheer to the nation, Petroleum Development Oman (PDO) 
                has discovered three new oil fields. Two of the new oil fields – 
                Taliah and Malaan West – are found amongst PDO’s Lekhwair 
                cluster of fields in northwest Oman. The third oilfield 
                discovery – Rabab Southeast – confirms the preliminary results 
                of well tests. First oil production from Rabab Southeast is 
                expected later this year, through the existing conventional 
                production facilities at Harweel, in south Oman. In addition, 
                appraisal wells drilled by PDO in 2008 have also revealed 
                additional volumes of hydrocarbons in existing oil and gas 
                fields.<br>
                <br>
                PDO’s total 
                <img border="0" src="oil2.jpg" align="right" hspace="8" vspace="6" width="183" height="313">production of oil and condensate increased to 
                633,000 bpd in 2008 against 607,000 bpd in 2007. Overall, for 
                the third year running, PDO produced in excess of one million 
                bpd of oil equivalent in the form of crude oil, natural gas and 
                condensate. <br>
                <br>
                On the prospects for 2009, PDO’s Managing Director John Malcolm 
                informed, “Our oil target for the coming 12 months is the same 
                as previous year: 540,000-560,000 bpd. Condensate production is 
                set to continue growing to 80-90,000 bpd, setting the scene for 
                another YoY rise in liquid hydrocarbon production. Gas 
                production is harder to predict because it depends on external 
                factors, notably the call on our gas from customers.”<br>
                <br>
                To meet the challenge of declining oil prices, PDO is reducing 
                its costs and improving efficiency. Malcolm added, “The change 
                in market conditions will impact positively on our costs – 
                capital as well as operating – which I anticipate will come down 
                further over the coming months. We are discussing with our 
                contractors to see how these changing market conditions can be 
                translated into lower costs for PDO over the next five year 
                period. By working collaboratively with our contractors we can 
                find ways of cutting costs without cutting corners.” <br>
