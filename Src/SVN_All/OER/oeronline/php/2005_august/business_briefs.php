                        <font face="Verdana" size="1"><b>Renaissance acquires 
                        BUE Marine UK</b><br>
                        Renaissance Services SAOG (RENAISSANCE), through its 100 
                        percent-owned subsidiary Topaz Energy and Marine Ltd., 
                        Dubai (TOPAZ) has acquired the entire shareholding of 
                        BUE Marine Ltd., UK (BUE). The deal was signed in 
                        Edinburgh on 15 July 2005. The outgoing shareholders of 
                        BUE included 3i Group plc, Dresdner Kleinwort 
                        Wasserstein Limited and three senior managers of the 
                        company. With this acquisition, the combined marine 
                        business of BUE and TOPAZ will rank among the top 10 
                        offshore support vessel operators in the world with a 
                        geographical footprint in the Caspian, the Middle East 
                        and S.E. Asia. RENAISSANCE had announced its intention 
                        to acquire BUE in June this year and has concluded the 
                        transaction in a matter of few weeks, thereafter.<br>
&nbsp;<br>
                        BUE has its headquarters in the UK and currently 
                        operates primarily in the Caspian region. Established in 
                        1991, BUE has historically provided and managed marine 
                        support vessels for the offshore oil and gas sector in 
                        the North Sea and the Caspian. In recent years, BUE has 
                        invested heavily and now operates a diversified fleet of 
                        42 vessels comprising supply vessels, anchor-handling 
                        tugs, accommodation vessels, icebreakers, work barges, 
                        and utility boats. As part of its ambitious fleet 
                        expansion programme, it has another 19 vessels and 
                        barges under construction in shipyards with further 
                        orders to follow. The enterprise value of the company, 
                        including the current new build programme underway, is 
                        in the region of USD 150 Million. BUE’s current 
                        operations are supported by Lloyds TSB plc and Bank of 
                        Scotland. <br>
                        <br>
                        TOPAZ is also in the offshore support vessel business 
                        and operates in the Middle East, S.E. Asia and West 
                        Africa. The BUE and TOPAZ customer base comprises almost 
                        all the leading international oil and gas companies such 
                        as Shell, BP, Total, Statoil, Agip etc. with both 
                        companies having established a reputation as quality 
                        fleet operators.<br>
                        <br>
                        The BUE Group employs over 600 people. Key senior 
                        executives including their CEO will continue with the 
                        enlarged group. TOPAZ has total employee strength of 
                        around 1,200 while RENAISSANCE has close to 3,000 people 
                        working for it in various businesses.<br>
                        <br>
                        RENAISSANCE acquired TOPAZ earlier this year to create 
                        one of the largest services companies in Oman with a 
                        diverse group of businesses. Based on the audited 
                        results, the combined turnover of the two companies for 
                        the year ended December 2004 was over USD225 Million 
                        with net profit in excess of USD40 Million. A key driver 
                        for the merger was to create a company, which would be 
                        able to grow rapidly in its principal businesses through 
                        acquisitions. BUE fits in with this plan and will 
                        contribute approximately another USD100M of annual 
                        turnover to the group with commensurate profitability. 
                        The acquisition has involved an investment of 
                        approximately USD40 Million via NICO Middle East Ltd., a 
                        TOPAZ subsidiary. This comprises purchase consideration 
                        for the shares as well as discharge of certain 
                        liabilities of BUE upon change of its shareholding. This 
                        transaction has been partially funded from additional 
                        borrowings by the group that have been provided by local 
                        and international banks. From a strategic viewpoint, BUE 
                        is operating in a market that does not overlap with the 
                        current operations of TOPAZ, with substantial potential 
                        for further growth in this region for other businesses 
                        within the RENAISSANCE Group.</font></td>
