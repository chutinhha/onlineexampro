<img border="0" src="athel.jpg" width="150" height="110" align="left">Please 
tell OER about your career path…</b><br>
I have worked only in the hospitality field. My first job was in the hospitality 
industry, when I was 13-year-old and in high school. I wanted to get away from 
home and the only way I could do that was to get a job. I convinced my dad to 
let me work as a dishwasher in a motel in San Francisco. It was my first 
exposure to the industry and also my best summer. The experience stayed with me 
and triggered an interest to come back into the field. So I went to a hotel 
management school after high school. <br>
<br>
I started my career in San Francisco in the sales department of a hotel. Since 
then, I have changed jobs 19-20 times. My career has taken me to different parts 
of the world. I have worked in North and South America, China, Hong Kong, 
Singapore, Indonesia, Saudi Arabia and now, here in Muscat. <br>
<br>
During my career, I have opened and closed hotels, taken over and given away 
hotels. I have worked in most of the divisions of a hotel, except the kitchen. 
My core competence is marketing and administration. It’s been a fascinating 
experience for me, the hotel industry. <br>
<br>
<b>Please share with our readers about the management structure of your hotel</b><br>
Most hotels generally have fairly similar and standard way of working. The 
General Manager heads the hotel and the day-to-day operations are usually split 
between the EAMs. We try to keep the levels as less as possible so that there 
aren’t too many layers to go through among the employees. <br>
<br>
<b>What are your responsibilities in the hotel?</b><br>
I am responsible for the operations of the hotel. My role entails generating for 
the owners and investors, the highest possible returns on their investment. And 
it is not just the Al Bustan Palace Hotel that I am responsible for, but also 
the others in the group, which includes the Inter Continental Muscat, Crowne 
Plaza, Crowne Plaza Salalah and Holiday Inn. I have to look after the operations 
of these hotels as well.<br>
<br>
<b>Can you briefly describe your management style?</b><br>
There is no particular management style that I follow. Every hotel has a 
different character. I have worked in so many hotels around the world, and the 
work culture changes from place to place. My work strategy involves letting 
others learn from experience and giving them enough freedom to work. For 
example, you can’t run a hotel in Muscat like you would manage one in Sydney or 
China. I adjust the management structure, depending on the hotel and 
environment. I do not try to make changes overnight. <br>
<br>
<b>In your opinion, what are the qualities of a good manager? </b><br>
A good manager would have a good management policy. He/she should have 
established guidelines and a work strategy. A good manager assigns each person a 
particular role and delegates responsibility. You have to let the employees do 
their work, once responsibilities have been assigned to them and not interfere 
unless absolutely necessary. A good manager should allow his employees to make 
mistakes, because that’s the only way they will learn.<br>
<br>
<b>What challenges has your hotel faced in Oman? And how you have tackled them.</b><br>
There are challenges for you everywhere and in every industry. Compared to the 
other countries that I have worked in, the challenges that I have faced here in 
Muscat are much less. One of the challenges that we faced at the hotel was high 
guest occupancy this year. In Oman, the industry as a whole has taken a massive 
jump. I arrived in December last year, and the hotel was virtually running full. 
And amazingly we ran full until May, which is not usual in the business. Full 
occupancy continuously in the hotel industry is a challenge, especially if you 
have not experienced it before. We had full guest occupancy right through from 
December 2004 to May 2005 and we were not prepared for it. So our staff had to 
work very long hours. They did double shifts as well as work back-to-back hours. 
Many of them could not take their weekly leaves and were always doing an 
enormous amount of overtime. All our staff were stressed, but they put up a 
great show and performed remarkably well. <br>
<br>
This was a learning experience for us and we are now prepared for next year. We 
know what to expect and we will be better equipped. <br>
<br>
<b>What is your opinion about the hospitality industry in Oman? Is the Sultanate 
ready to open its doors to tourists? </b><br>
The hospitality industry in Oman is quite good. We provide extremely good 
service and the industry as a whole is fairly advanced. Maybe we are not as 
advanced as the industry is in certain countries in South East Asia, but we are 
getting there. Hotels in Muscat, on the global level, are impressive and provide 
commendable service. <br>
<br>
The future of the industry in Oman is fantastic and I feel I am at the right 
place at the right time. The hospitality industry is all set to take off in a 
great manner. <br>
<br>
Oman is ready to open its doors to tourists and tourism. Yes, we do have a 
shortage of hotel rooms in the Sultanate. But, later this year, the Shangri-La 
will open, which will help tourism and in due course, the Wave will also be 
inaugurated, all of which will help the tourism sector immensely. According to a 
survey, the number of rooms in the Sultanate will double by 2008 and there will 
be a 40 per cent increase by 2010. There’s a lot to look forward to in Muscat.<br>
<br>
<b>Which management gurus do you favour and why?</b><br>
Honestly speaking, I am not big on self-help books. <br>
<br>
<b>In the seven months you have been here, what is your opinion of Muscat?</b><br>
I like the warmth and hospitality of the people. It’s incredible. I had spent 
some time earlier in the Middle East before, and my opinion then was different. 
But Muscat is amazing. And the people get the credit for that. They are 
friendly, generous and pleasant.<br>
<br>
The country itself is very beautiful. The topography is marvelous and though 
