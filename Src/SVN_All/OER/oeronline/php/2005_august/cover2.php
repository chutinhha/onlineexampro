            <p align="justify"><font face="Verdana" size="1">Obviously, it’s the global brands that tends to build a country’s 
            reputation or their dominance in a certain commodity or field of 
            activity.<br>
            <br>
            If you’re introducing a product that reflects the country’s 
            heritage, you have some built-in momentum. If not, you can have a 
            problem. For a high-tech Argentinean company, we recommended that 
            they make their U.S. office the headquarters and become an American 
            company instead of an Argentinean company. This turned out to be a 
            very successful strategy as they were able to take advantage of the 
            U.S. high technology heritage. <br>
            <br>
            Sometimes countries don’t take advantage of their heritage. China 
            could certainly build a big porcelain brand with their long history 
            in this category. I suggested to them that they bring back the most 
            famous porcelain brand in history. (Ming.) It would be a runaway 
            success.<br>
            <br>
            Spain has overlooked a heritage opportunity. They are the dominant 
            leader in olive oil production, but Italy is perceived as the king. 
            They actually sell more olive oil to Italy than Italy produces. 
            Spain should lay claim to being “The World’s No.1 Producer” of olive 
            oil and they should dramatize this with the following concept: “Two 
            thousand years ago the Romans were our best customers. Today they 
            still are.”<br>
            <br>
            Obviously, labeling and other marketing activities are called for, 
            but the opportunity is there for the taking. Interestingly, Spain is 
            just now undertaking such a programme. <br>
            <br>
            <b>The role of a national airline</b><br>
            Does a national airline contribute to the branding of a country? I 
            don’t think so. Airline brands have to stand on their own strategy, 
            which doesn’t often reflect on a country’s strategy. British Airways 
            isn’t about history, it’s about experience, an extensive route 
            structure and being ‘The world’s favorite airline to fly’. Singapore 
            Airlines is about a higher class of service, not about the country.
            <br>
            <br>
            Sabena, Belgian World Airlines, was involved in the three-star city 
            programme but it didn’t help the airline and they are no longer with 
            us. They didn’t have enough of a route structure to compete. Nor did 
            Air New Zealand. The bottom line: Countries are one set of branding 
            problems. Airlines are another. In this era of killer airline 
            competition, most flag carriers lose money that could be better 
            spent by a country in making it more attractive for other airlines 
            to fly there.<br>
            <br>
            <b>Why go to Oman?</b><br>
            For Oman to pursue a branding strategy, it has to first determine 
            whether it’s to pursue business, tourists or both? In either case, 
            you need a differentiating idea. Each decision will require a 
            different strategy. <br>
            <br>
            If the answer is tourists, you need to start with your competitor 
            which, in your part of the world, is Dubai. Such was the case many 
            years ago when I was working on developing a tourist strategy for 
            Belgium. Their big next-door competitor was Amsterdam and their 
            canals. What evolved was taking advantage of the fact that the 
            Michelin Guide rated cities as well as restaurants and Belgium had 
            five three-star cities. Holland had only one. Thus, people visiting 
            Holland’s three-star city, Amsterdam, were encouraged to also visit 
            the three-star cities in the country next door. <br>
            <br>
            With the right strategy and execution, Dubai and Oman could become 
            two places to see in the Middle East. Dubai has already done a lot 
            of work in this regard. Oman has a lot left to do. </font></p>
