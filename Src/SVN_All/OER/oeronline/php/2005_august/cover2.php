            <p align="justify"><font face="Verdana" size="1">These are powerful symbols where you don’t even have to see the name 
            of the country to recognize where they are located. Interestingly, 
            Dubai is in the process of establishing such a visual symbol with 
            their sail-shaped Burj Al Arab hotel. <br>
            <br>
            <b>A unique concept</b><br>
            If you don’t have a visual symbol that people want to see, you need 
            a unique concept that is worth going to experience. Great Britain 
            has their castles, stately houses and royal history. New Zealand has 
            amazing physical beauty in two of the most beautiful islands in the 
            world. Spain has the sun and is Europe’s resort country.<br>
            <br>
            The Caribbean island of Grenada is an excellent example of how you 
            work out a competitive strategy. Located at the bottom of the 
            Caribbean, it is a smallish (344 sq. km) and thinly populated 
            (100,000) island with agriculture as its main source of income. With 
            a high unemployment rate (25 per cent), they wanted to increase 
            tourism. But they had enormous competition from many other islands. 
            What they did have was unspoiled countryside, unspoiled beaches, and 
            no tall buildings. This set up a unique concept of being ‘The 
            Caribbean the way it used to be’. (Unspoiled.) Their competitors 
            could do little against this unique concept.<br>
            <br>
            <b>What’s in a name? </b><br>
            There are times when your name can be a problem in attracting 
            tourism. The Russians recognized that problem with a city called 
            Leningrad. After all, who wants to go see Lenin, especially after 
            communism fell apart? Changing their name back to St. Petersburg was 
            a brilliant move that enabled them to once again use history as an 
            attraction. Once again it was the city of the czars.<br>
            <br>
            Sri Lanka also suffers from a name problem. It conjures up political 
            unrest with the Tamil Tigers and the damage of the tsunami. My 
            advice to them was like the Russians, to re-brand the country with 
            their old name Ceylon. That enables them escape their negative 
            perceptions and once again capture the romance of the tea 
            plantations and their beautiful countryside.<br>
            <br>
            The Ukraine is a name that sounds more like a region than a country 
            and few people know where it is. My advice was to use a locational 
            strategy and position the country as ‘Middle Europe’. This is 
            exactly the direction they are pursuing by moving away from their 
            Russian connections in the last election.<br>
            <br>
            But that’s all to do with tourism, how about business?<br>
