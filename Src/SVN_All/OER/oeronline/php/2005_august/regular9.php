e-business: Back to Basics<br>
</font></b><font face="Verdana" size="4" color="#FF0000"><br>
</font><b><font face="Verdana" size="1">Matt Harrison explains the concept of 
e-business and how Oman is utilising e-business for the social and economic 
development of its people</font></b></p>
