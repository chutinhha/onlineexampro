<img border="1" src="marketing.jpg" width="200" height="119" align="right" hspace="5" vspace="2"></font><font face="Verdana" size="3"><b>“T</b></font><font face="Verdana" size="1">he 
significant problems we face cannot be solved by the same level of thinking that 
created them.” Albert Einstein.<br>
<br>
Technological change has been a major hallmark of economic and social 
development. A vital component of that technical change process has been the 
rapid evolution of the Internet as a basis for virtually instantaneous global 
communication. The expansion of an e-network has been explosive over the past 
ten years and it’s revolutionising the way in which business and government 
conduct their day-to-day activities. <br>
<br>
Indeed, Oman-based organisations, no matter how big or small, are learning to 
use the Internet to improve productivity and cut costs. Around the Middle East, 
we’re seeing the development of e-markets, many with a global reach and 
encompassing thousands of businesses. The e-mail systems are improving corporate 
communications, hierarchies are becoming flatter and we’re seeing the 
development of entirely new Internet-based corporate structures such as 'the 
virtual company'. Indeed, the Internet is levelling the global commercial 
playing field; a place where small to medium-sized enterprises (SMEs) can 
compete with much larger rivals in ways unimaginable ten or fifteen years ago. 
Thanks to the Internet, problems of geography and distance can be surmounted. 
Physically, Oman may be on the edge of Indian Ocean, but e-business and digital 
communications can place us well and truly at the centre of any business 
activity.<br>
<br>
In short, the 'have PC, will travel' mentality means customer and supplier 
relationships can become closer and more timely, and new markets just a mouse 
click away. Combined with their existing business systems, there’s huge 
potential for Oman-based firms and government ministries to capture 
customer/client data, refine and tailor products and services they offer, 
speed-up order fulfilment and build market share and customer/client loyalty. 
Moreover, as Oman’s telecommunications network improves and Internet 
possibilities become more widespread, one can anticipate that Omani companies 
and government ministries will become ever more integrated into the global 
economic system through their access and sophisticated use of the Internet.<br>
<br>
Oman is well-positioned to take advantage of the opportunities offered by 
e-business and is making great strides on many fronts - from a fully-occupied 
Technology Park in Rusayl to the various e-initiatives being rolled out by ITTS 
at the Ministry of National Economy - but is viewed in some quarters as not 
aggressively rising to the challenge. Oman faces a choice. Capitalise fully and 
quickly on the opportunities e-business presents, or remain producing modest 
success and risk falling behind other economies in the Middle East. In short, 
Oman can e-lead or e-lag. <br>
<br>
<b>What is e-biz?</b><br>
e-business can be defined as the use of electronic networks (either publicly 
accessible as with the Internet, or private as with an Intranet or Extranet) to 
undertake or support commercial activities. Four main aspects of e-business can 
be identified.<br>
<br>
1 .e-business - the buying and selling of goods and services over electronic 
networks, typically sub-divided into business to consumer (B-2-C) and business 
to business (B-2-B) activities. In future, it’s anticipated that such categories 
will become increasingly blurred. In the longer term, the distinction may well 
be between customer-facing and supplier-facing e-business, where a customer may 
be a business customer or consumer.<br>
<br>
2. Electronic administration - transferring administrative tasks which were 
previously carried out by paper and phone into an electronic network.<br>
<br>
3. Electronic communication - the use of e-mails and other forms of electronic 
messaging to transmit data and value-added telephony services such as 
video-conferencing, which require a sophisticated communications infrastructure.<br>
<br>
4. On-line information - placing information which was previously stored in, and 
disseminated through, paper documents on to the Internet or an Intranet or 
Extranet.<br>
<br>
In many cases, e-business simply replaces a physical process with an electronic 
one. But it also has indirect effects by enabling new ways of doing business.
<br>
<br>
Three important areas in which this is happening are:<br>
<br>
1. Flexi-working - this is facilitated by the greater ease of keeping in touch 
through conferencing and e-mail, and in being able to access and upload 
information from any location, including the home;<br>
<br>
2. Glass supply chains - critical data, such as levels of stocks and orders 
within the supply chain, can be automatically made available to all players via 
shared Extranets, creating much greater collective knowledge of where goods are, 
and what will be required in future. Today, many goods are produced only in 
response to automatically generated orders based on what’s actually selling in 
shops and/or being moved into them from warehouses - with the information being 
gained by scanning bar codes.<br>
<br>
3.Virtual organization - this occurs when many activities which were previously 
done in-house are outsourced or carried out in partnership with others, with the 
relationships handled partially or exclusively via Internet or Extranet 
linkages.<br>
<br>
Put simply, e-business is a subset of business, where products and services are 
advertised, bought and sold over the Internet. <br>
<br>
Oman-based SMEs have been relatively slow to adopt e-business strategies. 
Indeed, many SMEs don’t understand how e-business applies to their day-to-day 
activities. We need more events, workshops, seminars - like Knowledge Oasis 
Muscat’s monthly Open House Program - that demonstrates how e-business works, 
sessions that educate and encourage Oman’s public and private sectors in 
developing e-business strategies and practices. <br>
<br>
<b>Holding Up the Traffic: Macro Issues</b><br>
e-business holds a great deal of promise for Oman. But then it has done for some 
time now. For example, US and Europe-based IT experts, talk in glowing terms 
about e-business applications. But Oman, in particular, and the Gulf in general, 
appear to be lagging behind. Why is this? Here are eight points to consider.<br>
<br>
1. Over the past couple of years, we’ve all become accustomed to double-digit 
monthly growth in things Internet and anything growing more slowly seems to have 
stalled. <br>
<br>
2 .Let’s be clear, progress isn’t only slower than people would like it to be in 
Oman; it’s slower everywhere else too. <br>
<br>
3. Many of Oman’s large organisations change slowly. They have heavily engrained 
structures, cultures and processes. New developments take time to percolate. <br>
<br>
4. Oman’s SMEs, on the other hand, won’t take up new technologies quickly unless 
they perceive immediate advantage in doing so, or immediate disadvantage in not 
making the move. On a more positive note, technologies that have broken through 
the barrier and achieved rapid adoption are the fax-machine and the mobile 
phone. Both are important business tools, and, with the advantage of hindsight, 
will be perceived to have been early influencers on Oman’s e-business 
activities. <br>
<br>
5. Generally speaking, a critical mass of e-business-capable business partners 
is widely seen as a pre-requisite to greater e-business breakthrough in Oman. 
Oman’s SMEs have yet to see an increase in the numbers of business partners, who 
expect them to use elements of e-business, such as e-marketplaces for locating 
sources of supply and potential customers; web-forms for ordering; Electronic 
Data Interchange (EDI) for transmitting transaction data with suppliers and 
customers; e-mail for ongoing communications with customers; and Internet-based 
payment mechanisms for settlement. <br>
<br>
It should be noted that rapid change is afoot in this area, particularly with 
the work being carried out by organizations such as Knowledge Oasis Muscat – a 
fully occupied Technology Park with the Gulf region’s leading business incubator 
programme - PDO, Muscat Municipality, the One-Stop-Shop at the Ministry of 
Commerce and Industry and the Information Technology Technical Secretariat (ITTS) 
at the Ministry of National Economy. <br>
<br>
Indeed, ITTS has developed a Digital Strategy - one notable e-business related 
project that originates with ITTS is e-Legislation, which is expected to be 
ready by the end of 2005. This will cover:<br>
• Authentication Law <br>
• Open Records Law<br>
• Procurement Law <br>
• Privacy Law<br>
• e-Payment Law<br>
• Liability Law<br>
• Jurisdiction<br>
<br>
Other examples of Oman’s work in the digital arena include: The Ministry of 
Education’s development of a wireless network, which will cover more then 400 
schools across the Sultanate. The Ministry of Health is digitalising its 
processes. This plan will start with networking 47 hospitals, 135 clinics and 
health centres and other health-related government offices.<br>
<br>
6. For e-business tools to become Oman-mainstream, it’s important that 
initiatives be structured on a collaborative, 'win-win-win' basis, so that all 
parties share in the advantages and hence have positive incentives to 
participate, as well as negative disincentives not to. <br>
<br>
7. It’s apparent that Oman-based organisations - particularly SMEs - have only 
modest interest in doing small parts of their purchasing on the Internet. If 
they’re to adapt Internet-practices, then they want to see a rich set of 
Internet-capable partners and a considerable portion of their work performed 
within a consistent and coherent environment. <br>
<br>
8. Another often-quoted factor is inadequate security. The underlying factor 
here is: requisite trust in the reliability of e-business, and unleash rapid 
take-up. Digital certificates and certification authorities are also important 
parts of this jigsaw puzzle. Indeed, how may of us receive viruses and spam that 
slip through Omantel’s e-security systems?<br>
<br>
In brief, for e-business to really take hold in the Sultanate, it’s imperative 
that Oman’s business community and government bodies reflect on how they 
currently conduct business and the many ways in which the adoption of 
e-strategies could positively impact their bottom line. For example: <br>
• Time reduction - How long does it take to get a message or a document to your 
potential customer? With e-mail, it’s seconds. <br>
• Time currency - How current is the information your potential customers have 
on your products and services? Using the Internet, they can always access the 
latest and most current information. Several companies are doing this for 
product catalogues and also saving on print and distribution costs.<br>
• Time independence - With a telephone call, both parties have to be free and by 
the phone at the same time. With electronic communication, sending and receiving 
are asynchronous. It’s perfectly possible to send and reply to multiple messages 
each hour. <br>
• Time displacement - By using people during their normal working hours around 
the world, it’s possible to support your customers around the clock. <br>
• Distance diminishes - Reaching someone on the other side of the globe and 
sending information or exchanging messages costs virtually the same as someone 
locally. You don't need to pay expensive courier bills. <br>
• Spatial rearrangement - Why have suppliers visit your office? Companies can 
save costs, access skills wherever they are, provided they’re connected and have 
the necessary networking skills. <br>
• Reduced communications costs - With electronic networking, it costs virtually 
the same to send a message to 100 people as one. Costs are also (generally) 
distance and bulk independent. Therefore, you can reach more people for much 
less than using conventional mail - an e-mail message can be sent for 50 to 80 
per cent less than its postal counterpart. <br>
• New sequences of activities - Your supply and distribution chain can take 
advantage of shared resources (such as online databases) so that activities can 
take place in parallel, creating speed and cost advantages over sequential 
processing. <br>
• Supply chain reconfiguration - With careful thought, you can use electronic 
market places to cut out inefficiencies in your supply chain. Many companies now 
reach potential customers directly through the Internet. <br>
• New business networks - Using effective networking skills, it’s possible to 
create virtual corporations to meet specific customer needs or gain access to 
resources. These new dynamic relationships are giving companies flexibility and 
speed over conventional contracting relationships. <br>
These are just some of the many ways in which the Internet is altering the 
global business landscape.<br>
<br>
<b>Holding up the traffic: Micro issues</b><br>
The impediments to greater Oman-based e-business adoption, from a B-2-B 
perspective, can be summarised in the following terms: <br>
Organisational issues <br>
• Organisational resistance to change. <br>
• Competing priorities for executive and management attention. <br>
• Communications difficulties among executives, managers and people with 
relevant technical expertise.<br>
• Nervousness about the ability to collect debts and the reliability of goods 
delivery and services performance. <br>
<br>
<b>Inter-organisational issues</b><br>
• Resistance against multiple, incompatible requirements being imposed by major 
players in what are commonly referred to as 'hub-and-spoke' schemes, which 
inevitably involve some degree of duress on the generally smaller business 
partners, and frequently involve proprietary protocols, and impose costs on the 
smaller business which are specific to a particular partner, rather than shared 
across many partners.<br>
• The challenges of achieving a critical mass of electronically capable business 
partners, such that duplication of channels and processes can be avoided and 
real savings made.<br>
<br>
<b>Investment issues </b><br>
• Long cycles for re-investment in organisational infrastructure.<br>
• Competing priorities for financial resources.<br>
• Limited apparent payback on the investment required.<br>
• Difficulties in establishing a compelling business case. <br>
<br>
<b>Technological issues </b><br>
• Inadequate infrastructure, technology or services. <br>
• Proprietary, rather than open-standards-based infrastructure, technology or 
services, risking 'lock-in' to the initial providers because of excessively high 
switching costs to alternative providers in the future.<br>
• Poor fit between existing infrastructure, technology and services to the needs 
and practices of the particular organisation and/or of the industry/ies it 
operates in.<br>
• Limited availability, unreliability and cost of technical skills. <br>
<br>
<b>Payment issues</b><br>
• e-business is presaged on the assumption that the participants will pay for 
what they buy. But there has been a marked reluctance among Oman-based net users 
to actually part with their money, particularly for digital goods and services. 
As a result, much of the current business on the Internet is funded using 
business models other than user-pays, primarily advertising and sponsorship. <br>
<br>
<b>Trust</b><br>
• Consumers are seriously concerned about a number of dimensions of trust. Trust 
in the security of value passed during e-transactions with organisations that 
are 'virtual' in a disconcertingly ineffable way. The forms of value that are 
involved include: <br>
<br>
1. Money paid and received.<br>
2. Goods and services offered and acquired.<br>
3. Assurances that a refund is available for unsatisfactory goods and services.
<br>
4. Trust in the security of personal data while it is in transit during 
e-transactions. <br>
5. Trust in the privacy of personal data arising from e-transactions.<br>
6. Trust in the subsequent behaviour of the other party to an e-transaction. In 
particular, there is a fear based on prior experiences with marketers in other 
contexts that they’re likely to make unwarranted assumptions about the nature of 
any relationship that may arise from the transaction. <br>
This cluster of public confidence issues are not peculiar to the private sector, 
it also impacts on the e-services being delivered by government. Until and 
unless they’re effectively addressed, proponents of e-business and e-services 
delivery schemes in Oman will continue to be disappointed by low adoption rates.<br>
