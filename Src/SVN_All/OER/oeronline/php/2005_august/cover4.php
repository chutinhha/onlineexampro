            <b>How Country Brands Affect Behaviour: Perceived Value </b><br>
            Brands affect behaviour through ‘perceived value’. Perceived value 
            is the value a customer perceives a product or service to possess. 
            Perceived value determines a customer’s actions. For example, if the 
            price of a product or service is above what the customer considers 
            its perceived value to be, no purchase will take place. <br>
            <br>
            Perceived value can be thought of as consisting of two major 
            components: the economic component and the brand component (see 
            exhibit 1).<br>
            <br>
            The economic component would be what is sometimes referred to as 
            “value-in-use” in discussions of pricing. For example, if an airline 
            is considering purchase of a new model of aircraft engine, 
            considerable attention would be given to the economic benefits that 
            engine can provide in regard to fuel and maintenance costs. However, 
            the perceived value of the engine would also reflect a brand 
            component - what is contributed to the perceived value by the 
            reputation of a General Electric, Pratt &amp; Whitney or Rolls-Royce.<br>
            <br>
            For some products and services, the country of origin may increase 
            the perceived value of the product or service. For example, Germany 
            is widely regarded as having a reputation for products that are 
            well-engineered, France for products that are luxurious, Italy for 
            products that are stylish, and Japan for products that are reliable. 
            The reputation of some countries may decrease perceived value. <br>
            <br>
            Perceived value can be measured. Statistical techniques are 
            available that allow the estimation of overall perceived value and 
            also permit the estimation of the relative importance of the 
            economic and brand components shown in exhibit 1.<br>
            <br>
            <b>Building a Country Brand</b><br>
            To build a country brand, one must understand the components of a 
            brand. These are: identifiers, attributes and associations (see 
            exhibit 2.)<br>
            <br>
            Identifiers consist of all the signals that identify a brand such as 
            name and logo. For countries, identifiers would include the country 
            name, but also the country’s flag and other symbols, even 
            country-related icons such as the Pyramids, Mount Fuji, or the 
            Sydney Opera House.<br>
            <br>
            Attributes are any characteristics. They might be features or they 
            might be benefits. For countries, the attributes may be product 
            reputations such as German engineering prowess or personalities such 
            as Australian candor. Attributes should be chosen to achieve country 
            objectives.<br>
            <br>
            <img border="1" src="cover_st.jpg" align="right">Associations are the connections between the identifiers and the 
            attributes. Strong brands have strong associations.<br>
            <br>
            To build a country brand, one must:<br>
            • Select a target market - customers or perhaps investors.<br>
            • Determine the desired country brand attributes - the attributes 
            that must be communicated to members of the target market.<br>
            • Design the identifiers that will be the cue for the attributes.<br>
            • Develop and implement a communications campaign that will connect 
            the identifiers to the attributes within the minds of the members of 
            the target market.<br>
            • Monitor the country brand to ensure that the country brand will be 
            an important part of the overall perceived value to the target 
            market member.<br>
            <br>
            <b>Country Brands and Investing</b><br>
            Not only can country brands affect purchase of a country’s products 
            and services, but they can also affect investments made in that 
            country. For investing, the economic component of perceived value 
            would be determined by the financial analysis of a project. However, 
            the brand reputation of the project management company - or of the 
            country where the project is located - could positively or 
            negatively affect the perceived value associated with that 
            investment project. If the investment required for a project is 
            above what an investor considers its perceived value to justify, 
            then no investment will take place.<br>
            <br>
            To conclude, positive country brands can provide powerful stimulus 
            for both exports and investments. To build positive country brands 
            requires a clear strategy as to what attributes need to be 
            associated with the country’s identifiers. Then, a communications 
            programme needs to be implemented, which consistently builds the 
            desired associations over time among the members of the customer or 
            investment communities in the country’s target markets.</font></p>
