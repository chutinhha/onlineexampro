            <p align="justify"><b>
            <font color="#004000" face="Verdana" style="font-size: 14pt">
            Location is Oman’s USP<br>
            </font><font color="#004000" face="Verdana" size="1"><br>
            HH Faisal Bin Turki Al-Said, Head of Planning &amp; International 
            Relations, The Omani Centre For Investment Promotion &amp; Export 
            Development (OCIPED), in a conversation with Akshay Bhatnagar, talks 
            about how Oman is building on its strengths to enhance its brand 
            equity<br>
            </font></b><font face="Verdana" size="3"><b><br>
            W</b></font><font face="Verdana" size="1"><b>hat are the strengths 
            and weaknesses of Oman as a brand?</b><br>
            I think Oman’s basic strength is its unique location. Most of the 
            people, who have made Oman as their business base, is because of its 
            advantageous location. That’s one thing which differentiates us 
            clearly from our neighbouring countries. Oman is the gateway; it’s 
            the link between east and west for trade. Thereafter, comes our 
            tourism potential with long coastline and diverse natural riches. 
            After that, you go into the depth, in terms of what we have done to 
            build on our strengths. The economy is growing with Oman making good 
            use of Salalah Port, Sohar Port and the Sultan Qaboos Port. More 
            investments are being pumped in expanding and modernizing the 
            existing ports and developing new ones. If Oman has to build on its 
            strength, it has to be on its location and definitely tourism 
            potential. And concentration on these two alone will lead to 
            development in other potential industry sectors automatically. <br>
            <br>
            Do you think Oman’s political stability and friendliness of its 
            people are also the tangible strengths of the country?<br>
            I do agree that political stability and friendliness of Omani people 
            are our great strengths. Most of the expatriates and foreign 
            tourists find Oman to be a very friendly country. We have one of the 
            friendliest cultures in the region which attracts businessmen from 
            across the world. In their quest to become more advanced, many 
            countries are losing touch with their own culture which is not the 
            case with Oman. We have retained the uniqueness of our culture along 
            with making rapid strides on the development front. If you drive a 
            few kilometres away from the modern life in Oman, you will feel the 
            soul of Oman. That’s something we could definitely build on.<br>
            <br>
            <b>Oman is known as the ‘Essence of Arabia’. Don’t you think it’s 
            time to move beyond being the essence of Arabia?</b><br>
            Yes, it’s time to build on something even more. When you talk of the 
            essence of Arabia from the perception of a Westerner, it basically 
            means deserts, camels and Arabian nights. But you find all these in 
            other Middle East countries also. So the essence of Arabia is really 
            an understatement for Oman now. The country has a lot more to offer 
            with its unique culture, heritage, cosmopolitan environment and 
            friendly atmosphere. That’s what we are trying to build on. <br>
            <br>
            Landor Associates (the global branding company given the assignment 
            to work on the branding of Oman), in their initial recommendations, 
            has advised that Oman has much more to offer besides being the 
            essence of Arabia. It’s time we make the world more aware of Oman’s 
            strengths like our diverse and matchless tourist destinations. <br>
            <br>
            <b>Do you think Oman is perceived differently viz-a-viz other 
            countries in Middle East?</b><br>
            We are still trying to build ourselves in a different mould and 
            making efforts to convey our unique positioning viz-a-viz our 
            neighbours. Honestly speaking, those who have not been to the 
            country or don’t know about us have no clue about how we are 
            different from other Middle East countries. Oman has not been 
            perceived the way we would like to get perceived. Again, that’s the 
            reason why the Government is making these efforts (appointment of 
            Landor Associates to work on Oman branding) to create a distinct 
            identity for ourselves. <br>
            If you travel around in Europe, America, Africa and Asia barring 
            Middle East, you will find people even confusing Oman with Amman 
            (the capital of Jordan)! <br>
            <br>
            <b>Do you think Oman has lost out by not projecting a unique 
            landmark to give the country a ready visual recall?</b><br>
            It’s difficult to say. Maybe, the branding exercise which we have 
            embarked on, might lead us to exactly what you are saying! We might 
            identify a visual relevance also. So when you think of Oman, a 
            particular visual imagery might pop up instantly. <br>
            <br>
            Talking of Oman’s unique aspects, it lies in its rich heritage and 
            culture. For example, forts. You won’t find the number of forts we 
            have in Oman, anywhere in the Middle East. Each fort tells its own 
            story. That’s why when it comes to tourism, we emphasize a lot on 
            visits to our forts. There are lots of other things as well, but the 
            point is how significant they are from the global perspective and 
            how they can contribute to Oman’s image.<br>
            <br>
            Frankincense (a type of aromatic gum) is definitely another 
            attraction for sure. Historically, Oman has been known as the land 
            of Frankincense. The best Frankincense in the world comes from Oman. 
            Again the point is how many people know about this. Sometime back, a 
            Japanese tourist group came to Oman and was absolutely amazed by 
            Oman’s Frankincense. They took Frankincense from here to Japan and 
            started manufacturing it there in different ways to be presented as 
            gifts. <br>
            <br>
            Salalah also is unique. You won’t find such natural beauty in Middle 
            East. Though Middle East is known for high temperature and humidity, 
            but in peak summer, Salalah offers a pleasant European or Asian kind 
            of weather with its greenery and rains. When we tell foreigners, 
            especially Europeans, that Oman has such a beautiful and pleasant 
            place, they don’t believe it. They say, just a few hundred 
            kilometers away from so much of heat and barren mountains; you have 
            a place like that! <br>
            <br>
            <b>So how you are building on the strengths of a natural gift like 
            Salalah?</b><br>
            Salalah is a major attraction for tourists not just from Oman, but 
            other parts of the world also, especially the Middle East. On the 
            business front, Salalah Port is among the top international 
            transshipment seaports in the world. That’s another success story. 
            Further infrastructure expansion is taking place there. Numbers of 
            companies are showing keen interest in setting-up their base there 
            for re-export activities. By setting up a base in Salalah, the 
            investor gets three major benefits. First, enjoy the beautiful 
            weather; second, do business within the free zone; and third, 
            utilize the port for exports. It results in savings of time and 
            money. So Salalah is set to thrive with so much development taking 
            place there. From the marketing perspective, it basically depends on 
            how you play with the elements for promoting the location. <br>
            <br>
            <b>Do you think the communication strategy needs to be made more 
            focused and aggressive to market Oman?</b><br>
            We convey the message quite vigorously in the business meetings and 
            international industry forums. But mass media has a larger role to 
            play in creating more awareness and bringing forth the true picture 
            of Oman, not just in front of the international community, but the 
            domestic market as well. The development of local mass media has 
            played an excellent role in transforming the fortune of many 
            countries by generating constant exposure at the international 
            level. Hopefully, in the next 3-5 years, Oman’s media scene will 
            also graduate to the next level and we will also have the strength 
            to host more world-class events. <br>
            <br>
            We are already moving in this direction strongly. Muscat has been 
            chosen as the venue for an international conference scheduled in the 
            first quarter of next year. The organizers wanted a Middle East city 
            to host the event for the first time and they preferred to choose 
            Muscat. The organizers are expecting not less than 1,000 delegates 
            comprising high-level executives from the top Fortune500 companies. 
            The event organizers are expected to make a public announcement soon 
            on this. The high voltage event will be covered in almost all the 
            major media. That will market Oman itself by putting the country on 
            the global radar. This is marketing Oman indirectly. <br>
            <br>
            <b>Which are the other major developments that are enhancing Oman’s 
            brand equity? </b><br>
            We have been getting lot of enquiries from international business 
            community especially investors who want to set up base and acquire 
            properties in Oman. Shangri-La Barr Al Jissah Resort is a shining 
            example of the joint ventures with international partners in Oman. 
            It’s absolutely world-class resort, offering the best of facilities. 
            I believe in two months, they are going to have the soft launch, and 
            in the first quarter of the next year, they will be having 
            full-fledged operations. From what I hear, they are already booked 
            for the first quarter of next year! They brought European travel and 
            tour company officials to visit them and the Europeans couldn’t 
            believe that there is something like this in Oman.<br>
            In next five years, Oman will be a different place altogether. A new 
            terminal will be ready in the next few years at Seeb International 
            Airport. Salalah Airport will be upgraded to a world-class 
            international airport. Then there are multi billion dollar cities 
            like the Blue City and the Wave coming up too. <br>
            <br>
            <b>A section of the international community believes that Oman has 
            been slow in reaction as the competition is heating up. What do you 
            think about this? </b><br>
            See, Oman has progressed remarkably in a very stable way. Under the 
            wise leadership of His Majesty, Oman has stepped forward in a very 
            steady mode to ensure that development takes place in a rapid but 
            sustainable manner. Through five-year plans, the Government has made 
            admirable progress in different sectors in a phased manner. Rome was 
            not built in one day!<br>
            <br>
            While developing a robust economy, one cannot overstep growth 
            stages, as this would have a negative influence on the 
            socio-economic structure of the country. Take tourism for example. 
            Tourism is going to drive Oman in its quest to be among the leading 
            economies in the world. We are further developing our range of 
            world-class tourist spots in the country. But we are treading with 
            caution by not over promising or over selling ourself as a tourist 
            destination, currently, in the international tourism market. We may 
            not be able to handle more traffic beyond our infrastructure 
            capacity. As I told you that we are further expanding our airports, 
            few years down the line, our airports will be ready to handle 
            traffic upto 12 million annually. That is a huge number. That’s why 
            steps have been taken, with complete precaution that we have to grow 
            our parallel infrastructure too. This approach is going to make Oman 
            a balanced, sustainable and growing economy nation.</font></p>
