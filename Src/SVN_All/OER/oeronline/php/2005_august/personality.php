it comes to setting goals, he firmly believes that only sky can be the limit. 
And claims anyone can reach Himalayan heights provided he or she has the right 
approach. His formula for success: be bold, seize the opportunities and work as 
hard as possible. It’s perhaps a proof of his perseverance and hard work that he 
hasn’t taken a proper vacation since 1980, the year he launched his business. 
There are many words that can be used to describe Salim Ajib Silim Al Hajri, but 
laidback certainly isn’t one of them. As Chief Executive Officer of the highly 
successful Ajib Trading Co LLC, Ajib is the perfect example of that popular old 
saying: success is one percent inspiration and 99 percent perspiration. <br>
<br>
<b>Humble Beginnings</b><br>
Ajib grew up in Tanzania, where he did his schooling. “I came from a very poor 
family,” says the entrepreneur, “and everything I did I had to start from the 
scratch and build up.” But Ajib was not the one to get bogged down by such 
circumstances, and carried on with his studies with great determination. After 
completing his Bachelor of Science in Engineering in 1976, he decided to move to 
Oman to make his living. He started his career here, working as a clerk in a 
bank. After a year-and-a-half, he moved on to doing administrative work at 
another company, before making his mind up to take a huge risk to fulfill his 
ambition. Although he had been promoted to Personnel Manager at his job, Ajib 
resigned in 1980 to start his own company, Ajib Trading. <br>
<br>
“We started with a very small set-up. It’s hard to believe it but I started the 
venture with only RO250,” Ajib says, smiling. “We started with one division for 
welding. At that time, in the 1980s, construction was happening everywhere and 
the business was booming in Oman. A lot of steel structures were needed so I 
decided to pursue welding. After a while, we expanded from building construction 
into road construction, and then, to transportation. We just kept growing, and 
we have now been doing business in this country for 25 years!” <br>
<br>
<b>Dealing with Difficulties</b><br>
Running a huge company with a wide variety of successfully functioning divisions 
is no easy task, and no one knows this better than Ajib. “There have been great 
challenges!” he reflects. “There are lots of ups and downs in business. There is 
a lot of competition; stiff competition from companies that do similar business 
in Oman. Now, the biggest challenge in transportation, construction and moving 
equipment is the task of getting the right people to handle the equipment. The 
problem we’ve had with operators in the past is that we trained them, and once 
they finished training, they would leave and go to Dubai or Saudi Arabia, 
thinking they’d be paid more there. Whether this is true or not, it is important 
to have good, qualified workers staying here in Oman.” <br>
<br>
Ajib firmly believes that problems will only make his business stronger in the 
long run. “In life, challenges shouldn’t be avoided,” he states. “You meet 
challenges with the hope of doing better.” In the past three years, Ajib Trading 
has registered a growth rate in revenue of 15%. Within the next two years, this 
rate is expected to climb up to 25%. Evidently, Ajib has faced the challenges 
head on and his business has thrived. <br>
<br>
<img border="1" src="perso2.jpg" width="120" height="98" align="left" hspace="4" vspace="1"><b>Company 
Policy</b><br>
With so many different divisions working at the same time, it’s difficult to pin 
down a uniform set of policies set in place at Ajib Trading. However, certain 
basic conventions do apply across the board. “We stick to quality jobs,” Ajib 
says. “There is a scale of jobs and the jobs we take have to be above a certain 
level. There is a minimum contract we’ll take. <br>
And we take jobs from customers we know or whose backgrounds we know, because we 
have had big problems in the past with companies that defaulted on payments.<br>
<br>
“In operations, our focus is first and foremost on HSE - the health, safety and 
environmental standards of the company. The mission of HSE policy is to operate 
a safe business, and this should not be taken lightly. As for employment, we 
prefer to take people fresh out of universities and colleges because we can 
groom them and train them so they acquire our specific operational requirements 
and can function well within the company. This is especially important for young 
Omanis straight out of university. We give them valuable training and we know 
they will turn out to be achievers.”<br>
<br>
<b>Outside the Office </b><br>
“I am married and I have five children,” Ajib says with evident pride. “Three of 
them are studying in the U.S. One is doing electronics, another is doing 
accounting, while the third one is doing graphic designing!” Balancing a highly 
demanding career and family life is a challenge faced by many businesspeople, 
and Ajib is no exception. “Ah, it is difficult. It’s very difficult, but I 
appreciate that my wife understands the way we operate because with more than a 
hundred operations, under so many divisions - we have a health club, we have 
road construction, building construction, transport and so on - my time is 
really occupied so much in the business. My family sees me much less than they’d 
like to and I am able to spend much less time with them than I would like to. 
This is sad but I can’t avoid it and my family understands it. I do spend some 
time with them, especially on weekends, when we go on very short holidays. It’s 
another matter that I usually come back early, without them. That’s the kind of 
life we lead.<br>
<br>
“I don’t even have time for hobbies!” he reveals. “But that is a necessary part 
of a successful business. This company started from scratch. I’ve built it up 
from virtually nothing to where it is now, and my presence is required here to 
keep it running. I haven’t stayed away from this business for more than a week 
or two. I haven’t been away on a month-long holiday for the last 25 years!”<br>
<br>
<b>The Key to Success</b><br>
Ajib’s achievements go beyond his impressive business. In 1986, he became an 
Honorary Consul of Tanzania, representing Tanzanian interests in Oman. “I was 
issuing visas to people who want to go to Tanzania and giving advice on how to 
invest in Tanzania, as well as organising cultural activities between Tanzania 
and Oman,” he says. “So it’s like being an ambassador, only there is no embassy 
in this country yet!” Then, there is the philanthropic side of the man. He has 
opened the Muscat Health Club, which gives free treatment to people suffering 
from various ailments. He also runs a cashew nut factory in Tanzania. <br>
<br>
Not many people have managed to handle so many different activities as 
effectively as Ajib. “It is difficult to do business, but I’m happy that I’ve 
achieved this because I’ve been working really hard! And I’d like to see others, 
especially the young people, do the same.” <br>
<br>
To the youth trying to break into the business world, he has these words of 
advice: “You shouldn’t straightaway look for a comfortable and secure 
employment. You can take a risk and start from the scratch and do something 
really good. We started with a very small set-up, and see where we are now!<br>
<br>
“Oman is full of opportunities,” he says. “There are so many possibilities. What 
is really needed is hard work. If you work very hard, you can achieve so much 
more because there is so much more available now and many more options. Anyone 
who sets goals and works hard for it can succeed. But setting a goal is very 
important. If you do not have a goal, you’ll just keep working your whole life 
and success will elude you. “So, figure out what your goals are, and go the 
whole hog for it,” he advises.
