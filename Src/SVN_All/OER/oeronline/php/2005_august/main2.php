</font><br>
<font face="Verdana" size="1"><b>In an exclusive chat with OER, James Best, 
Chief People &amp; Strategic Officer, DDB Worldwide and Group Chairman of DDB UK, 
Middle East and Africa, talks about the challenges for advertising in Middle 
East and the changing dynamics of media<br>
</b>
<br>
<b>
<img border="1" src="face_t_face3.jpg" width="100" height="133" align="left" hspace="4" vspace="1">How 
has been your journey in advertising so far? </b><br>
I began my advertising career almost thirty years ago as a planning trainee at 
Boase Massimi Pollitt in London, the pioneer of account planning. Advertising 
promised to be a fast-moving job with lots of variety, in a creative environment 
and with the opportunity to be part of contemporary popular culture. <br>
<br>
BMP went public in 1983 and was bought in 1989 by Omnicom, to become BMP DDB. I 
was chairman throughout the 1990’s, when we rose to be the UK’s second largest 
agency, and then moved to DDB’s corporate team, first in Europe and then beyond.
<br>
<br>
<b>What role do you play in your current job profile?</b><br>
My role now as Chief People and Strategic Officer of DDB Worldwide, is one of 
seven individuals in the network’s Executive Committee, under Chief Executive 
Ken Kaess. I also represent our Middle East and Africa interests on the 
executive, to ensure that this region, like the others, has a voice at the 
centre.<br>
<br>
My ‘people’ role is wonderful. DDB’s management teams everywhere are urged to 
make their priorities; people, product, profit - in that order, recognising the 
fact that people should come first in our business - after all, what else do we 
really have? To unleash the potential of our people and through them of the 
brands and businesses they work on, is our mission and I am very lucky to be at 
its heart. Our people around the world are incredibly talented, committed and 
energetic; helping to create a culture that can sustain and develop them and 
their skills is a contribution worth trying to make.<br>
<br>
<b>How long have you been associated with the Middle East market? </b><br>
DDB has had a good presence in the Middle East since the early 1990’s, although 
my involvement has been more recent. Today we are well-spread, with 11 offices 
across the region, including Oman, Saudi Arabia, UAE, Bahrain and Kuwait in the 
Gulf.<br>
<br>
<b>In what aspects is it different from the other markets you have worked in?</b><br>
The region, like any other in the world, has a combination of unique as well as 
universal features. The Gulf countries are, of course, all prosperous, 
relatively young and characterised by rapid development. They have a familiarity 
with the best brands from around the world, which are freely accessible here. 
The economies here are more trading in nature rather than manufacturing driven. 
So they tend to be more sales-focussed than classic brand marketers. The 
short-term promotions often dominate over the brand-building campaigns, which 
may be seen as the responsibility of the principals that the importer 
represents.<br>
<br>
But each country in the Gulf clearly has it unique character. Arabic may be the 
common language, but the nuances of meaning and accents vary like national dress 
and cultural history. Oman is self-evidently a very different place from Dubai, 
after all!<br>
<br>
<b>What are the challenges which are unique to this region?</b><br>
There are some particular challenges. In media terms, the region differs from 
others in my experience in having such strong pan-regional channels (especially 
the satellite broadcasters) as well as local stations and titles. This gives 
rise to a complex media-planning environment. The large proportion of 
expatriates, although it varies from country to country, is also a unique 
regional feature. The expats come from many countries - albeit mostly Asian - 
and backgrounds, making for a complexity of consumer segments.<br>
&nbsp;<br>
With expats usually running the advertising business in the Gulf markets, too, 
they have to take pains to develop an understanding of local culture and 
sensitivities. Translating local ads written originally in English into Arabic 
is not the ideal way to develop work sympathetic to a particular culture. To my 
mind, recruitment of more local talent to work in advertising is a priority 
throughout the region.<br>
<br>
<b>So what is your strategy to overcome these challenges?</b><br>
DDB’s approach to the region has been to find local entrepreneurs to become our 
partners. DDB Oman, with Radha Mukherji as the executive director, for example 
is a part of the OHI Group of companies. This approach, which acknowledges and 
respects local roots, complements our development of a regional hub in Dubai. We 
seek to provide training via DDB University to our partner agencies in the 
region, to ensure a thorough understanding of DDB’s beliefs, approaches and 
skills. This way, we aim to nurture local talent.<br>
<br>
We also believe that creativity can be the most powerful tool in business, and 
try to help our partners nurture, direct and discipline their creativity to get 
better results for their clients.<br>
<br>
<b>With almost all major international brands already present in the region, how 
would you go about promoting a local brand?</b><br>
In promoting any brand, local or international, in a market we would seek to 
understand the local consumer and the competitive context. DDB has a suite of 
tools for brand understanding and direction, as well as communications planning, 
activation and evaluation, which we use to guide our work to be relevant, 
original and impactful. There is no formula, but we find that simplicity, 
surprise and a smile often characterise successful advertising executions.<br>
<br>
<b>Do you think that mass audience is finally giving way to more fragmented 
segments, especially with the emergence of new media?</b><br>
For 2004, DDB was - for the second year running - AdWeek’s Global Agency Network 
of the Year. The award cited our ability to go beyond traditional creativity in 
mass media to deliver communications for global clients as diverse as Exxon 
Mobil and McDonald’s.<br>
<br>
At the same time, DDB topped in the Gunn Report’s compilation of creative awards 
from around the world during 2004. And at the recent Cannes Awards, DDB bagged 
44 Lions! We see no contradiction in excelling in mainstream media vehicles like 
30-second TV commercials as well as online and direct marketing.<br>
<br>
In essence, the communicator’s choice has widened, just like in the case of a 
consumer. We can choose mass, niche or personal media to reach our client’s 
target audiences, depending on which is appropriate and efficient. A mass-market 
extravaganza such as the US Superbowl, in which DDB ads claimed the top three 
places in consumer approval, can be cost-effective for brands seeking to make an 
immediate positive impression on a huge audience. Meanwhile, viral online 
marketing preceding the Superbowl ‘warmed up’ the audience by creating intrigue 
and talk value for the ads shown in the game.<br>
<br>
Personal media such as e-mail and mobile phones, or shared experiences like 
online gaming or blogs, can all become vehicles for marketing communications - 
and effective ones, provided marketers do not abuse their access to consumers or 
act so intrusively as to provoke a backlash. As technologies develop, so 
advertising evolves to use them. Good advertising has always won over its 
audience through entertainment, humour, empathy or simple usefulness, and the 
same principles hold true for new media vehicles, too.<br>
<br>
<b>Do you foresee a convergence of the advertising and entertainment business in 
the future? </b><br>
The ‘Madison &amp; Vine’ convergence of the skills of the advertising and 
entertainment industries is a developing story. The ‘soap operas’ of the 1950’s 
were sponsored or branded entertainment, so nothing really changes! Fifty years 
later, we see advertising-supported programming and film-making re-emerging. The 
role of the ad agency will continue to be to creatively connect client and 
consumer via the bridge of understanding both and representing each to the 
other. <br>
<br>
We see ourselves as the providers of creative solutions to business issues - no 
matter what the nature of the solution or the channel it uses, creative thinking 
and market insights will fuel our continued success.<br>
<br>
<b>What’s your take on the consolidation process going on in the global 
advertising landscape in the last few years? </b><br>
Globally, consolidation is often considered a natural consequence of free market 
capitalism. As the market economy has globalised, so has consolidation, whether 
on the part of client companies or the agencies that serve them around the 
world. Organic growth has quickened in the last two years and looks healthy for 
the global advertising business in 2005. <br>
<br>
As advertising agencies add to their professional competence by broadening their 
channel expertise, so organic growth can increase for DDB and its competitors as 
they help their clients grow. </font></p>
