Japan could turnout to be behind the making of the world's fastest 
supercomputer. It is planning to begin research next year on a new machine that 
would operate 73 times faster than IBM's current record holder, as per a Japan 
Government spokesperson.<br>
<br>
Currently, the American Blue Gene/L system supercomputer which is developed by 
International Business Machine (IBM) Corp. at Lawrence Livermore National 
Laboratory in Livermore, California, is the world's fastest supercomputer. The 
machine is capable of 136.8 teraflops, or 136.8 trillion calculations per 
second.<br>
<br>
Japan is exploring the possibility of developing a supercomputer that can 
operate at 10 petaflops, or 10 quadrillion calculations per second, which is 73 
times faster than the Blue Gene, as per sources. <br>
<br>
The total cost of the project is expected to range between $714-893 million. An 
official firm decision on the project will be taken sometime in August thus 
year. If approved, the next-generation supercomputer will be ready by sometime 
in 2010-11. It must be remembered that Japan's Earth Simulator supercomputer, 
introduced in 2002, had been the world's fastest until 2004, when the IBM's Blue 
Gene took the title. <br>
<br>
Currently, the Earth Simulator, at a speed of 35.9 teraflops, is ranked fourth 
after the IBM's two Blue Gene systems and NASA's Columbia system, all in the 
United States, according to the top 500 list of the world's fastest 
supercomputers, released at the International Supercomputing Conference held in 
