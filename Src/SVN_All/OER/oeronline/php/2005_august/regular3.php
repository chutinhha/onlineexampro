    <b>HP dismantles 4 R&amp;D groups</b><br>
Following its restructuring targeted at eliminating its global workforce by 10 
per cent, Hewlett-Packard has dismantled its four research groups at HP Labs. HP 
has dropped a team based in Palo Alto focused on advanced software research led 
by Alan Kay, a respected computing pioneer. The technology major has also 
dissolved two Palo Alto labs that dealt with emerging technologies and consumer 
applications and a group in Cambridge, Mass., that handled research on health 
care and medical issues. <br>
<br>
In a related move, the company has also consolidated the HP Labs organization by 
eliminating a level of management that ran three separate research centers for 
printing and imaging research, Internet and computing platforms, and information 
technology services. Kay is widely respected as one of the pioneers of personal 
computing who also helped design the ARPAnet, the forerunner of the Internet. He 
has won two of the computing industry's highest honors, the Association for 
Computing Machinery Turing Award and the National Academy of Engineering Charles 
Stark Draper Prize. <br>
<br>
Despite the cutback in R&amp;D activities, HP has stressed that it remains committed 
to innovation through research and development, citing HP Labs' continuing work 
in the fields of nanotechnology and quantum computing. As per a company 
spokesperson, “The message is (that) we are trying to refocus our research into 
the areas of greatest promise and our core strength and those areas which are 
most important to HP in the medium and long term. <br>
<br>
Despite these cuts, we still have a rich and varied research agenda. We're still 
doing a lot of interesting and very important work. There just comes a time when 
you have to set priorities.&quot;</font></p><hr size="1">
