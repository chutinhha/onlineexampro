    <b>
<img border="0" src="giz2.jpg" width="100" height="115" align="right" hspace="5" vspace="2">Mobiles 
to become No.1 entertainment gadget</b><br>
LG Electronics has predicted that mobile phones will overtake all other 
entertainment consumer electronics in the next three years as the pace of 
development in handset features becomes ever more rapid. According to 
Seoul-based Dr. Kang Tea Ik, Chief Research Engineer for LG Mobile 
Communications: “The next generation 3.5G LG mobile phones will open up a whole 
new world of possibilities, and will come equipped with multimedia broadcasting 
functions allowing users to watch terrestrial broadcasts and talk on the phone 
at the same time.”<br>
<br>
Dr. Kang predicted that data transfer speed will soon reach 10 Mbps and new 
handsets will provide converged technologies capable of broadcast functions 
(DMB), video-on-demand, video conferencing, and interactive broadcasting. The 
expansion of mobile phone capability will also spur major content improvements, 
with developers soon introducing 3D story games and multiple-player games for 
both on and off-line mobile users.<br>
<br>
“When it comes to broadcasting, various DMB mobile phones are already available 
in mature markets like the US, Japan and Korea,” Dr. Kang said. “This technology 
will receive a further boost when LG Electronics’ 3.5G (HSDPA) handset arrives 
in the United States, Europe and Japan in the first quarter of 2006.”<br>
<br>
With worldwide handset sales forecast to reach one billion a year by 2009, 
according to analysts Gartner, advanced technology phones are expected to occupy 
an increasingly lucrative part of the global mobile market.<br>
<br>
LG has to its credit - manufacturing the world’s first PTV (Push-To-View) 
technology allowing multiple users to share live video in real time; the first 
DMB handset; the first synchronous/asynchronous combined 3G handset; and the 
slimmest CDMA mobile phone.</font></p><hr size="1">
