<p align="justify" dir="ltr"><font face="Verdana" size="1"> 
National Bank of Oman can be considered as the main reason behind the steep fall 
in the Banking Sector Index as it constitutes nearly 17 per cent of it. Not 
withstanding the RO4.450 per share paid by the Commercial Bank of Qatar, the 
stock price tumbled to close at the psychological level of RO4. Unlike the 
Banking and Investment sector, the losses in the industry Index was pronounced 
across the companies. Al Khaleej was the largest loser with a 13 per cent fall 
after it resumed its trading after the fire incident at its manufacturing plant. 
While the run of Cement companies came to an end as Raysut &amp; Oman Cement lost by 
7 per cent and 5.6 per cent respectively during the period under review.<br>
<br>
<b>Services sector becomes the star performer</b><br>
Inspire of the disappointing performance from the two largest sectors on the MSM 
Index, the traders could have been less disappointed, if they had played 
relatively safer by putting money into the services companies. The Index grew by 
2.21 per cent during the period to close just below the 2100 levels. The support 
for the services sector came from the oil marketing companies which have shown 
excellent results for the second quarter of 2005, notably Shell Oman. <br>
<br>
Al Maha and Oman Oil Marketing also improved with a 9.4 per cent and 1.6 per 
cent gain respectively during the period under review. Apart from it, other 
stocks include Al Jazeria Services, Dhofar Insurance and Salalah Port Services 
also improved <br>
<br>
<b>MSM Index Reconstituted</b><br>
During this period, the Muscat Securities Market has been recomposed with effect 
from July 3, 2005. From the Banking and Investment sub-group, Bank Dhofar, Gulf 
Investment Services and Global Financial Investments have been replaced by Oman 
Investment &amp; Finance, Oman &amp; Emirates Investment Holding and Al Sharqiya 
Investment Holding. Similarly, Al Anwar Ceramic Tiles, Al Hassan Engineering and 
Salalah Mills have been replaced by Al Jazeera Tube Mills, Al Anwar Holding and 
Oman Chromite. While under the services and insurance subgroup, Oman United 
Insurance, Topaz Energy and Marine and Oman Oil Marketing Company have been 
replaced with AES Barka, Al Maha Petroleum and United Power.<br>
<br>
Apart from this, MSM management has also decided to transfer Raysut Cement, 
Muscat National Holding, First Mazoon Fund, Oman &amp; Emirates Investment Holding, 
Muscat Index Fund, AES Barka, Dhofar Power and Al Kamil Power from the parallel 
market to the regular market. Salalah Port Services and Dhofar Insurance have 
been transferred from regular market to parallel market, while Hotels Management 
Company has been transferred to third market from the parallel market. Majan 
Glass and Gulf Plastic Industries have been transferred to the parallel market 
from the third market.<br>
<br>
<b>Omantel Issue</b><br>
Omantel issue, the primary focus of the investors during this period, emerged as 
the most successful issue in the history of the MSM markets, right from the 
opening day. The activity for the IPO issue picked up just prior to the closing 
date as large applicants started to subscribe to the shares. <br>
<br>
Against the total issue size of RO202 million on offer, nearly RO620 million was 
collected, thus making the issue oversubscribed by more than three times. <br>
<br>
<b>Way forward</b><br>
The short-term outlook for the markets would be guided by the response to the 
Omantel IPO issue which is expected to get listed on the markets during the 
early August 2005. Even if one considers that Omantel issue gets listed at the 
IPO price, the market capitalization would further increase by a staggering 33 
per cent. Omantel would definitely be ranked number one in market 
capitalization. This boost in the market capitalization should also help in 
reducing the overall dependency of banking companies on the local markets, 
thereby reducing the risks for the foreign investors, who are looking for 
opportunities with a strategic perspective. Also, the money is expected to 
remain in the local market as the large market capital companies like Omantel, 
Bank Muscat (which will together constitute around 36 per cent) along with the 
companies like Oman Cement, Result Cement, Alliance Housing Bank have been 
improving their earnings largely from their sustainable operational side. This 
will fuel the overall volumes generated in the system and improve the liquidity 
in the system. And as seen before, with liquidity at normal level, the overall 
Index also improves. Hence, we believe that stock picking could be the norm for 
the next month and companies which have the visibility on sustaining their 
earnings growth momentum are likely to reward investors in the medium to long 
term.</font></p>
