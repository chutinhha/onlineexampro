<p align="justify" dir="ltr"><font face="Verdana" size="1"> 
The market is easing a little bit after the stupendous run during the first five 
month of 2005. Omantel issue running through most part of the period also took 
its toll on the market momentum. The barometer of the MSM markets measured by 
the MSM Index saw a drop of nearly 5 per cent during the period (26 June – 21 
July 2005) with the Index which had been near the 5700 levels during the last 
week of June’05, starting to weaken and closing below the 5400 levels at the end 
of the period<br>
<br>
<b>Banking sector pulling down the markets</b><br>
If one observes the performance of the markets from the mid of June’05, the 
markets have climbed from the 5300 levels to reach the peak of 5700 level before 
retracking back to the starting level of 5300 points. Of all the three indices, 
the banking sector tumbled the maximum with nearly a 7 per cent fall.</font></p>
