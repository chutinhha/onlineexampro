<p align="justify" dir="ltr"><font face="Verdana" size="3"><b>I</b></font><font face="Verdana" size="1">t 
is important to draw a distinction between the levels of development between 
different Arab States from the Maghrib countries in the West and the Gulf 
Corporate Council countries in the East and those in the middle. These countries 
use different policies and are subject to different external exposures and hence 
their approach to development differs considerably. Therefore, the net result is 
also very different from one country to another.<br>
<br>
However, the Arab World - State of Affairs described in this report, is a result 
of clubbing all the Arab states as though they were one country and evaluates 
its human resources as both subject and object of development in all its 
dimensions - economic, social, civil, political and cultural. It provides a 
neutral forum to measure progress and deficits, propose strategies to 
policymakers and draws attention to country problems that can benefit from 
regional solutions. It is guided by the conviction that solid analysis can 
contribute to the many efforts underway to mobilize the region’s rich human 
potential. <br>
<br>
<b>Foundation of Progress</b><br>
There has been considerable progress in laying the foundations for health, 
habitat and education. Two notable achievements are the enormous quantitative 
expansion in educating the young and a conspicuous improvement in fighting 
death. For example, life expectancy has increased by 15 years over the last 
three decades, and infant mortality rates have dropped by two-thirds. Moreover, 
the region’s growth has been “pro-poor” - there is much less dire poverty 
(defined as income of less than a dollar a day) than in any other developing 
region.<br>
<br>
But there have been warning signals as well. Over the past 20 years, growth in 
per capita income was the lowest in the world, except in sub-Saharan Africa. At 
a growth rate of 0.5 per cent annually, if such trends continue in the future, 
it will take the average Arab citizen 140 years to double his or her income, 
while other regions are set to achieve that level in a matter of less than 10 
years. Labour productivity has been low and is declining. <br>
In fact, total factory productivity declined at an annual average of 0.2 per 
cent during 1960-1990, while it rapidly accelerated in other parts of the world. 
Compared to the Asian Tigers, per capita output was higher than average of this 
group in 1960. Now, it is half of that in Korea. The productivity of Arab 
industrial labour in 1960 was 32 per cent that of the North American level. By 
1990, it had fallen to 19 per cent. <br>
<br>
The decline in workers’ productivity has been accompanied by deterioration in 
real wages, which has accentuated poverty. It is evident that in both 
quantitative and qualitative terms, Arab countries have not developed as quickly 
or as fully as other comparable regions. From a human development perspective, 
the state of human development in the Arab world, is a cause for concern.<br>
<br>
Achievements by the Arab region on the Human Development Index (HDI) in the past 
decade were lower than the world average. Compared to other regions, the Arab 
world does better on income indicators. Thus, it can be said that the Arab 
region is richer than it is developed. Although income poverty is low compared 
to other parts of the world, the Arab region is hobbled by a different kind of 
poverty - poverty of capabilities and poverty of opportunities. These have their 
roots in three deficits - freedom, women’s empowerment and knowledge. Growth 
alone will neither bridge these gaps, nor set the region on the road to 
sustainable development.<br>
<br>
The way forward involves tackling human capabilities and knowledge. It also 
involves promoting systems of good governance, those that promote, support and 
sustain human well-being, based on expanding human capabilities, choices, 
opportunities and freedoms (economic and social) as well as political, 
especially for the poorest and most marginalized members of society. The 
empowerment of women must be fully addressed throughout.<br>
<br>
<b>The People</b><br>
The combined population of the 22 Arab countries covered by this report was 280 
million in 2000, 5 per cent of the world. Arabs are younger than the global 
average - 38 per cent are aged 0-14 years. Migration within, from and to the 
Arab region, is an important demographic feature, as is urbanization. Half the 
population lives in cities, compared to a quarter in 1950. Growth rates are 
still high - there will be between 410 and 459 million Arabs in 2020, with a 
slightly older age structure than that of today. The new demographic profile 
will present both challenges and opportunities for Arab countries. Population 
size, growth and age distribution can be either a demographic gift or curse, 
depending on whether countries can use the human potential represented by their 
populations well enough to satisfy people’s aspirations for a fulfilling life.<br>
<br>
<b>Health</b><br>
People in most Arab countries live longer than the world average life expectancy 
of 67 years. However, disease and disability reduce life expectancy by between 
five and 11 years. Arab women have lower life expectancy than the world average, 
partly due to high maternal mortality rates. Disability is higher for females 
than males, which points to women’s health as a priority for policymakers. <br>
<br>
Some progress has been made in reducing mortality rates for children under five. 
The Arab region was the first in the developing world, where most countries 
reduced mortality rates of under-five children to the target of 70 per thousand 
by 1990, well ahead of the global goal. But there are still large disparities 
among and within Arab countries in under-five mortality rates, with rural areas 
comparing unfavourably to urban ones. A major health challenge consists of high 
rates of halted growth, reaching 50 per cent of under-five children in some poor 
and war-torn countries.<br>
<br>
Road accidents and tobacco are significant causes of death. 182,000 Arabs died 
from tobacco-related conditions in 1998. HIV/AIDS is lower than in other 
regions, but is still a cause for concern. Most Arab countries spend around 4 
per cent of GDP on health, less than the middle-income country average of 5.7 
per cent. The vulnerable groups could be at risk in current health sector 
reforms.<br>
<br>
Health systems in Arab countries generally focus on physical, rather than mental 
or social health and hence they tend to neglect measurements for lack of 
comfort, lack of contentment and perceived health disorders. In many Arab 
countries, the elderly can be at a disadvantage as social security benefits for 
them are very limited and insufficient.<br>
Health systems also focus mainly on curative health in Arab states, especially 
at the secondary and tertiary levels. This leads to increased cost, especially 
with the resort to advanced technology; and leaves a significant portion of the 
population without adequate services. The challenges to health systems in many 
Arab countries include not only raising overall standards, but also ensuring 
greater fairness and fewer disparities.<br>
<br>
<b>Habitat</b><br>
Fifteen Arab countries are below the water “poverty-line” - less than 1,000 
cubic metres per person per year. Land-based pollution is creating coastal zone 
degradation that is costing countries $1-2 billion a year in lost tourism. The 
cultivated land per capita in Arab countries dropped from an average 0.4 
hectares in 1970 to 0.24 hectares in 1998. The conflicts and wars have 
aggravated pressures on the environment. In addition, there are environmental 
problems of global dimensions, with the erosion of the ozone layer at the 
forefront. There is an urgent need for environmental protection strategies to 
halt environmental degradation and promote cleaner production.<br>
<br>
<b>Education</b><br>
Arab countries have made tangible progress in improving literacy - adult 
illiteracy dropped from 60 per cent in 1980 to around 43 per cent in the mid 
90s; female literacy rates tripled since 1970. Yet 65 million adults are 
illiterate, almost two-thirds of them women - this is not expected to disappear 
for at least a quarter century. As a group, Arab countries spend a higher 
percentage of GDP on education than any other developing region. By 1995, over 
90 per cent of males and 75 per cent of females were enrolled in primary 
schools, and nearly 60 per cent of males and nearly 50 per cent of females were 
enrolled in secondary education. However, about 10 million children between 6 
and 15 years of age are out of school. </font></p>
