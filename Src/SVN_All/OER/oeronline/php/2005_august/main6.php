<p align="justify" dir="ltr"><font face="Verdana" size="1">All this implies weak 
economic and social returns on education. If the present rate of progress were 
maintained, decades would elapse before illiteracy is eradicated or enrolment in 
education reaches the level attained by developed countries in the mid 90s.<br>
<br>
<b>Employment</b><br>
The economy, creating full employment and reducing poverty - GDP in all Arab 
countries combined stood at $531.2 billion in 1999 (less than Spain at $595.5 
billion). The successful stabilisation in the 90s led to low inflation and 
budget deficits, and governments have provided much growth-supporting physical 
infrastructure. Yet, growth rates remain stagnant and overly vulnerable to oil 
price fluctuations. By 1998, the real income of the Arab citizen adjusted for 
purchasing power parity had on average fallen to 13.9 per cent of that of the 
OECD citizen.<br>
<br>
As noted earlier, Arab countries have the lowest level of dire poverty in the 
world (measured at less than $1 a day), together with low levels of inequality. 
Nonetheless, one out of every five Arabs lives on less than $2 per day. Poverty 
of capability is more pronounced as a result of the high rates of illiteracy, 
and inadequate access to quality education and learning.<br>
<br>
Open unemployment in the Arab region was estimated to be no less than 12 million 
in 1995, around 15 per cent of the labour force. If present rates continue, the 
number is expected to rise to 25 million in 2010.<br>
<br>
There are pronounced institutional impediments to employment generation in Arab 
countries. Labour markets are traditional, severely segmented and dysfunctional, 
labour-market intermediation, through employment exchanges, is ineffective. 
Structural adjustment packages have also played a role by paying insufficient 
attention to reforms that build competitive, efficient labour markets, an 
essential requirement for growth.<br>
<br>
The job opportunities and education were among the main concerns of the young. 
In a worrying trend, 51 per cent of older adolescents and 45 per cent of younger 
ones expressed a desire to emigrate, clearly indicating dissatisfaction with 
current conditions and prospects in their home countries.<br>
<br>
The cross-border and internal conflicts are serious obstacles to security and 
progress. The most pervasive is Israel’s illegal occupation of Arab lands, which 
denies the most basic Palestinian human rights. Beyond this issue and more 
broadly, the human and material damages of conflicts, sanctions and embargoes 
and their impact on the progress of peoples and nations, are incalculable. The 
failure of the Arab governments to find an acceptable solution to the 
Palestinian problem and the lack of a system where people could change the 
governments that have failed through peaceful means has created a number of 
conflicts within the Arab. More of the Arab resources are therefore spent in 
fighting counter government movements rather than investing in people.<br>
