<p align="justify" dir="ltr"><font face="Verdana" size="1">The enrolment rates 
in higher education remains limited to 13 per cent. Though higher than the 
average for developing countries (9 per cent), this rate is lower by far than 
those prevailing in industrialized countries (60 per cent). Moreover, the share 
of girls is noticeably limited, at the third (higher) level. <br>
<br>
Despite the rise in the number of children enrolled in pre-primary education in 
Arab countries, enrolment ratios are below their counterparts for developing 
countries, especially for females. More vulnerable social strata such as women 
and the poor, especially in rural areas, suffer more deprivation of education, a 
deprivation that tends to get accentuated at higher education levels.<br>
<br>
Downward pressures on government spending in the context of structural 
adjustment, together with inflation, the expansion of poverty and the 
introduction of educational fees as a cost-recovery measure, have had negative 
effects on the accumulation of human capital, with detrimental effects on the 
poor and women. The result has been a trend, whereby quality education is 
increasingly available only for the well-to-do. Hence the loss of education’s 
hitherto significant role as a tool of social mobility in Arab countries.<br>
<br>
There is evidence that the quality of education has deteriorated, implying a 
decline in knowledge acquisition and analytical and creative skills. The 
mismatch between the labour market and the level of development on the one hand, 
and the system of education on the other, results in weak labour productivity, 
imbalances in the wage structure, the preponderance of unemployment and the 
decline in real wages for the vast majority. </font></p>
