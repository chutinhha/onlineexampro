                    <font face="Verdana" size="4" color="#FF0000">GOING SOLO</font><p><font face="Verdana" size="1"><b>For the first time ever, two of Oman's most powerful businessmen Suhail and Saud Bahwan go on the record together. They meet with Oman Economic Review to talk about their careers, business models and their decision to trade as separate concerns.</b></font></p>
                    <p><font face="Verdana" size="2">We know Suhail and Saud Bahwan best as two brothers who teamed up to form a trading company that would become one of the largest business houses in Oman. The decision to disband the Suhail &amp; Saud Bahwan Group after 37 years of partnership to allow each of the brothers to focus on their own separate business ventures was understandably greeted with some surprise and considerable interest by international market players as well as the local business community. The move has resulted in the creation of two new business houses with clearly distinct corporate identities, different ambitions and new horizons - the Saud Bahwan Group and the Suhail Bahwan Group.&nbsp;<br>
                    <br>
                    The Suhail Bahwan Group has a workforce that is closer to 7,000 while the Saud Bahwan Group employs about 4,000 people. Each Group has a turnover of around US$1.0 billion. Combined, both companies' activities extend to touch almost every single person living in Oman.<br>
                    <br>
                    The brother's decision to end their business association is an amicable settlement, according to both camps. In an exclusive interview with OER, Sheikh Suhail says that the decision to disband was instigated to ensure that the businesses could be entrusted to their heirs in an easier fashion.&nbsp;<br>
                    <br>
                    There shouldn't be any major difficulties from the brothers parting companies although the level of competition that each may face from each other's Group holdings may pose a future challenge. Indeed, Sheikh Saud tells OER that, to all intents and purposes, each of them had been happily running the business houses as two independently managed entities very successfully for many years before the announcement to detach the Suhail &amp; Saud Bahwan Group though financial ownership used to be equally divided.</font></p>
                    <p><font face="Verdana" size="2"><b>Humble beginnings</b><br>
                    <img border="0" src="../../images/2002_november/bahwan.jpg" align="left" hspace="4" vspace="4" width="200" height="156">
 In 1965, the Bahwan brothers first established a shop in Muttrah Souq called Suhail and Saud Bahwan. They initially traded fishing nets, building materials and watches.&nbsp;<br>
                    <br>
                    Following the seafaring traditions of their ancestors, both the brothers had their initial trading experience aboard the vessels that plied the routes to West India and the upper Gulf of Oman from their native village Sur, an eastern seaport of Oman.&nbsp;<br>
                    <br>
                    Soon Suhail and Saud Bahwan turned their attention towards securing internationally reputed products. Their initial success was in 1968/69 with Toshiba and Seiko. Following the accession to the throne of His Majesty Sultan Qaboos and the greater growth of the national economy that followed, a demand for industrial and consumer goods quickly developed.&nbsp;<br>
                    <br>
                    The business acumen of the Bahwan brothers won prestigious franchises from leading international manufacturers and their marketing drive paid off.&nbsp;<br>
                    <br>
                    The brothers' aggressive marketing style coupled with their philosophy of professional after-sales service and a nationwide network of branches to reach consumers in the remote towns resulted in many of their products becoming market leaders."</font></p>
