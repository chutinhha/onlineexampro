                  <td width="100%"><font face="Verdana" size="4" color="#FF0000">Cash flow management</font><p><font face="Verdana" size="1"><b>In the aftermath of September 11, there is a renewed interest in fighting financial crimes. But how serious is the problem in the Gulf? Saleh Al-Shaibany finds out</b></font><font face="Verdana" size="2"><br>
                    <br>
                    <img border="0" src="../../images/2002_november/banking.jpg" align="left" hspace="5" vspace="5" width="179" height="200"></font><font face="Verdana" size="2">International experts have praised the efforts of Gulf countries in combating financial crime, but they urge that they must reduce if not eradicate the problem in the region - a feat that may prove impossible. According to industry statistics, banks in the Gulf recorded an average of US$14 million a year of illegal transactions in the six years up to 2000 as compared to US$620 billion lawful transactions in the same period. Although the latest figures are unavailable, joint efforts by the region's central banks have produced commendable results in cutting down the level of financial crime. In 2001, the Sultanate imprisoned senior officials of Oman Housing Bank for embezzlement charges as well as officials from Oman National Investment Corporation (ONIC) Holding Company SAOG, once a leading bluechip company that has recently regained its footing.<br>
                    Since 2000, all six Gulf countries set up new laws against money laundering. These boosted the financial security of their banks to allow maximum protection against many dubious transactions. "The region has made good progress in stamping out illegal money transfers but it also needs to be more transparent," Terence Donovan, Senior Economist, International Monetary Fund (IMF), told reporters during the Prevention of Financial Abuse Conference held at Muscat in September.&nbsp;<br>
                    In Oman, the local financial institutions have reported no abuses but called for the support of the business community to fight against it. His Excellency Hamood bin Sangoor Al Zadjali, Executive President, Central Bank of Oman, asserts, "We have received no reports of specific financial abuse. We must prevent it and we need shareholders, company boards, senior managers and auditors to be aware of the problem." Other officials declare that Oman would be tough on financial criminals to protect its economy and promote investments. "We are one of few countries in the region to comply with many of the recommendations of FATF (Financial Action Task Force), the Paris based international money laundering watchdog," avers Dr Abdulmalik Al Hinai, Economic Undersecretary, National Economy Ministry, and Chairman of Oman's Money Laundering Committee.&nbsp;<br>
                    The Sultanate is relatively less prone to financial abuse than other GCC states, say analysts. "There have been only 92 cases of suspicion in Oman between 1995 and 2000," informs Lt. Col Abdullah Al Harthy, Deputy Director Public Relations, Royal Oman Police. An average of 15 cases have occurred each month during this period but Al Harthy did not give figures for activity in the past two years. He did reveal to OER, however, that Oman's loss due to financial crimes during this period was as low as US$6 million, although the total volume of transactions between 1995 and 2000 is also unavailable for comparison.<br>
                    Criminals misuse financial institutions to bring dirty money from drugs, kickbacks, embezzlement and corruption into legal circulation. "The true figure is anybody's guess," a Dubai-based banker said. "Official statistics are just the tip of the iceberg. I would say not less than US$100 million in dirty money changes hands every year in one guise or another," he added.&nbsp;<br>
                    Claire Lo, President of FATF, asserts, "The Gulf banks need to keep vigilant and tighten the net on other forms of money transfers." The FATF was established in 1984, and has nearly 30 countries, including the GCC states, in its membership.<br>
                    <br>
                    <b>Hawala under scrutiny</b><br>
                    Lo was referring to 'hawala', an ancient system of money transfers which is prevalent in the Gulf and South Asia. Most of the hawala transfers go through unlicensed money-changers. It is based on trust and leaves no trace of the transactions. "This kind of money finds its way to countries like India, Pakistan and Bangladesh where it is not easy to detect," the Dubai-based banker said. The system is very popular with the eight million foreign workers in the Gulf, mainly from South Asia, and channels US$20 billion annually, according to GCC statistics.<br>
                    Indian Economic Minister, Yogesh Khanna, said last month that his country wants to restrict this informal system of transferring money from the Middle East. "The hawala has to be dealt with as something which should not be encouraged," he declares.<br>
                    However, similar calls have been met with caution in Pakistan, a country that receives about US$4 billion a year from its expatriates in the Gulf. "There are over 4,500 unlicensed money changers across Pakistan. Many of them do not have the required capital to get incorporated. They will just go underground and it will get even harder to track the transactions," says Malik Muhammad Bostan, President of the Forex Association of Pakistan (FAP).<br>
                    US Treasury Secretary Paul O'Neill, speaking to reporters earlier this year during his Gulf visit, said, "Our concern is that in many places they (hawala operators) don't have the regulatory processes of official banking systems." But experts are unsure as to what extent criminals abuse the hawala. No one knows how many traders practice the system and they tend not to keep records. The fact that hawala operates informally is one reason why financial criminals may be difficult to trace in places like Dubai. Gulf officials feel that the system only needs proper regulation. "It is a cheap and popular way to send money home for many people. We are monitoring it like financial institutions and so far we have seen no problems here in Oman," Zadjali states.<br>
                    There are nearly 3,000 money changing shops in the Gulf and bankers feel they will only be driven underground if officials decide to ban them. The problem will then aggravate. It will also cause hardship and panic among low-income foreign workers. They will view it as personal vendetta instead of a measure to curb illegal transfers, because banks charge steep commissions for sending money home. "It won't work and many of my colleagues in the financial community agree," the banker added.<br>
                    <br>
                    <b>Islamic finances</b><br>
                    The Gulf has been under scrutiny since September 11, 2001 for its allegedly lax financial system. The US contends that terrorists used banks in the UAE and Saudi Arabia to transfer illegal money to accomplices to plan the attacks in New York and Washington.<br>
                    In August, lawyers in the US filed a suit seeking damages of over US$100 trillion from Saudi banks and Islamic charities for the families of September 11 victims. They also called for a freeze on Saudi investments in the US, which are estimated at US$750 billion. "This is an act to extort Saudi money and a way of meddling in the region," said an official of Al Rajhi Investment and Development Corporation, one of the several Saudi financial institutions named in the lawsuit. In an October meeting of Gulf interior ministers held in Muscat, a press release said that the West has been unfair by its aggressive campaigns against member states (GCC), particularly Saudi Arabia.<br>
                    The US government has raised concern that Islamic financing methods can be used to channel funds for militant groups such as Al Qaeda. The objective of O'Neill's visit to the region was to learn more about Islamic banking. "Money that people give to help other people should be channelled to charitable purposes and only for charitable purposes," O'Neill said. "It is an unfounded allegation and the most outrageous one," a Bahraini Interior Ministry official retorted. "It won't help the financial investments in countries like Bahrain where we operate tens of millions of dollars in Islamic finances."<br>
                    Bahrain is a banking and financial hub, home to more than 100 banks and Islamic financial houses that have combined assets topping US$100 billion, while Saudi Arabia alone has hundreds of charities operating in the world, including in the US and some European countries. The global market for Islamic banking and finance is estimated by analysts to be worth US$200-500 billion and growing at 10-15 per cent per year.<br>
                    However Britain, which is not as sceptical as the US, said it would welcome applications to set up banks based on Islamic principles and talks have been held on how such a bank could meet their regulations. Speaking at a conference on Islamic finance, Howard Davies, chairman of Britain's Financial Services Authority, said there was a gap in the market for retail sector Islamic banking products, which could cater to nearly two million British Muslims. "We would welcome a soundly financed and prudently managed Islamic financial institution in this country, which would be good for Muslim customers, good for innovation and diversity in our markets and good for London as an international financial centre," Davies said recently.<br>
                    Many devout Muslims do not save in conventional banks because they regard fixed interest payments as usurious and the Islamic religious law, Sharia, forbids usury. In the UK alone, there are about 1.8 million Muslims permanently resident with estimated savings of around one billion pounds, while over half a million Muslims visited Britain last year, spending nearly 600 million pounds. So the potential market, whether for savings products, borrowings, or simply transaction related finance, is very large. It emerges that effective regulation and vigilant mechanisms for the full spectrum of financial services is just what the doctor has ordered.&nbsp;</font></p>
