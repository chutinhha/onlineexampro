                  <td width="100%"><font face="Verdana" size="4" color="#FF0000">The bilateral boost</font><p><font face="Verdana" size="1"><b>With the Oman India fertiliser project attaining financial closure and being expected to be completed by 2005, both the countries are now looking forward to setting up similar joint ventures. OER reports</b></font></p>
                    <p><img border="0" src="../../images/2002_november/ferti-1.jpg" align="left" hspace="5" vspace="5" width="175" height="178"><font face="Verdana" size="2">The Oman-India fertiliser project, which was first conceived in 1993, appears to be finally taking off with the project attaining financial closure. The US$969-million fertiliser is a joint venture (JV) between the government of Oman and Indian cooperative enterprises, Krishak Bharati Cooperative Limited (Kribhco) and Indian Farmers Fertlisers Cooperative (Iffco). The project, christened as Oman India&nbsp;<br>
                    Fertiliser Company (Omifco), is expected to be up and running by March 2005.<br>
                    In the US$316.5 million equity of the JV, Oman Oil Company, a 100-per cent closed joint-stock company owned by the government of Oman, holds 50 per cent, while Kribhco and Iffco have 25 per cent stake each. The plant will be operated and maintained by Omifco, while technical and managerial services, and personnel will be provided by the Indian partners.<br>
                    Omifco has finalised agreements with the government of Oman for the supply of natural gas, which will be the feedstock for the fertiliser plant. It has also signed an agreement with the government of India for the guaranteed offtake of urea and with Iffco for offtake of ammonia.<br>
                    <br>
                    <b>Improving ties</b><br>
                    The Oman-India fertiliser project is of great significance for both the countries, economically as well as politically. Economically, it was important to get the project off the ground as India is unable to meet its gas and energy requirements. Politically, it would be a boost to the bilateral relationship between India and Oman, which suffered minor setbacks recently due to the Dabhol fiasco and the problems related to Bharat Petroleum's Bina Refinery.&nbsp;<br>
                    After the Dabhol Power Company (DPC) in India's Maharashtra suspended power production following a rate dispute with the state electricity board, the gas imports from Oman for the Dabhol plant were put on hold. As for the Bina refinery, Oman was to play a major role with an equity stake, which is now a closed chapter. But following the financial closure of the fertiliser project, both the countries are now looking forward to a rewarding economic relationship.<br>
                    Sukhdev Singh Dhindsa, India's Minister of Chemicals and Fertilisers, on his recent to visit to the Oman said, "This project would be a matter of pride for all of us, and its successful completion would set the foundation for Oman and India for future bilateral cooperation. I appreciate the significance attached to this project by the government of Oman and the promoters." Salim Hassan Macki, chairman of Omifco, told a newspaper that with the wholehearted support and assistance of the government and the promoters, the project would be completed on schedule. "This should result in Oman and India establishing similar joint ventures," Dhindsa added.&nbsp;<br>
                    <br>
                    <b>Where the money comes from&nbsp;</b><br>
                    Omifco has successfully arranged a US$644 million debt facility in July this year and an agreement was signed in London. The lead-mandated group, comprising three international banks - BNP Paribas, ANZ Investment Bank and Arab Banking Corporation - had successfully underwritten the US$644 million facility for the project. Of the total arrangement, US$325 million is Export Credit Agency (ECA) covered loans and US$319 million, non-recourse commercial loans. The repayment period for the ECA loan is 10 years from the date of completion and eight and half years for commercial loans. Of the total ECA covered loans, Italy's SACE has committed US$210 million and France-based Coface US$115 million. The balance US$316.5 million will be promoters' equity. Stand-by facilities include US$28 million, stand-by equity, and US$28 million stand-by debt.&nbsp;<br>
                    The debt-equity ratio of the project is 67:33. The project would significantly benefit from the expertise of Indian promoters, Iffco and Kribhco, the two large cooperatives of Indian farmers with a track record of successful completion of such large projects and, of course, their profitable operations.&nbsp;<br>
                    The other banks involved with the project are the National Bank of Oman, Arab Bank, Arab Petroleum Investment Corporation, Bayerische Landes Bank, Emirates Bank International, Fortis Bank and Oman International Bank. The plant in Oman will be built by Snamprogetti of Italy and Technip of France on a turnkey contract. It will produce 1.65 million tonne of granulated urea and 2.5 lakh tonne of surplus ammonia. "Construction of the plant is to be completed in 35 months which would be April 2005," says an Iffco official.&nbsp;<br>
                    The potential<br>
                    The project is important for the Indian fertiliser industry, which is still highly subsidised. According to industry estimates there would be a gas shortfall of 3.5 million metric cubic meters gas per day (MMSCD) for the fertiliser projects along the HBJ (Hazira-Vijaipur-Jagdishpur) pipeline, India's first major in-land cross-country gas pipeline traversing the states of Gujarat, Madhya Pradesh, Rajasthan and Uttar Pradesh. In the Uran region in western India too, the supply is expected to fall short of demand by 2.9 MMSCD. In the coastal state of Gujarat, where there are two fertiliser plants, Gujarat Fertiliser and IFFCO Kalol, with a total capacity of 1,189,000 tonne, the gas shortfall would be in the region of one MMSCD. The total shortfall of gas for the fertiliser sector along the HBJ, Gujarat and Uran region is estimated to be around 7.4 MMSCD by 2004-05.<br>
                    The project also represents a major step in India's efforts to source chemical fertilisers from areas where the feedstock is available in plenty and on the basis of a long-term contract that would ensure stable prices. The fertiliser contract with Omifco is for 20 years, at fixed prices for the first 10 years and at a pre-agreed escalation for the next 10 years. The Oman project will supply urea to India on a long-term reducing basis starting with US$150 per tonne and gradually at a price of US$80 at the end of 15th year. The government of Oman and Omifco have also signed a gas supply agreement, under which Oman will supply gas to the project for 15 years at fixed price of US$0.77 per million BTU (British Thermal Units) for the first 10 years, and at a pre-agreed escalation for the remaining period, said U S Awasthi, Managing Director of Iffco. The requirement of gas for the project is four MMSCD.<br>
                    The fertiliser project is being constructed in Sur Industrial Estate, near the Oman LNG plant which will supply the feedstock. Setting up a production unit, whether it's a power or fertiliser unit, at the pit head has always been economically viable. For instance, carting coal from a mine to produce power at some distant land or transport gas in a liquefied state and regassifying it to produce fertilisers will be an expensive proposition. Another interesting feature of this project is that the Indian equity partners Iffco and Kribhco have given a guarantee for total offtake of the produce.<br>
                    <br>
                    <b>The price point&nbsp;</b><br>
                    Experts are of the opinion that fertiliser prices may remain high in the coming years. During the 1990s, barring 1993 and 1994 when price tumbled following Russia and other former Soviet republics dumping their export surplus (caused by drastic reduction in domestic consumption following sudden withdrawal of subsidies) and again during 1990 and 1999 when there was ban on urea imports by China (besides India substantially reducing its imports), the prices have remained high. They generally have been above the benchmark level of US$110 per tonne, at which the production cost would be fully covered.<br>
                    The trends in future will be broadly similar. In fact, consequent to projected significant decline in global urea surplus from 3.59 million tonne (MT) in 1999 to 2.11 MT during 2003, price is expected to increase sharply. Following entry of China into World Trade Organisation and the resultant freeing of imports, a large portion of its high cost domestic capacity is likely to be wiped out. This would lead to further tightening of global situation and, in turn, higher prices. According to officials in the Fertiliser Association of India (FAI), the representative body of fertiliser manufacturers in India, the fertiliser industry has a domestic capacity of 22 MT and an average domestic consumption of 20 MT. But fertiliser consumption is expected to grow by roughly four tonne per annum.&nbsp;<br>
                    <br>
                    <b>Advantage Oman</b><br>
                    Consequently, in the next five years the additional capacity in Oman will be fully used, assuming that there is no expansion of the existing facilities. The long term pricing policy which is being adopted by the Indian government can have collateral impact on many plants and create a vacant space for Oman to occupy. One fall-out of this is that the expansion of Indian fertiliser companies could be affected, for instance, those of Rashtriya Chemicals and Fertilisers' Thal and Kribhco's Hazira units. Considering that India's current fertiliser policy does not encourage private players, the government has very little elbow room to manage output and demand. "The demand-supply management would have to be managed well so that it doesn't shoot its own foot in Oman," says an FAI official.<br>
                    On the pricing front, since the Indian fertiliser industry is highly subsidised it would have a special pricing arrangement, akin to the retention pricing scheme. According to this pricing structure which is based on the principle of cost plus formula, prices decline as the plant depreciates and loans are paid off. The fertiliser produced from Omifco is expected to cost more than US$100 per tonne, which is currently the retail price cap. Experts feel that to some extent this price could be attained only by providing part subsidies.<br>
                    However, fertiliser prices are expected to decline over time and with it there will be a reduction in the subsidy levels. As the Indian government is moving towards rationalising fertiliser prices by gradually dismantling the retention pricing scheme, international sourcing of fertilisers at competitive prices would become more and more important. Since the market for fertilisers can turn volatile if large spot trades are placed, the long term price contract envisaged in Oman-India fertiliser project will be beneficial for both the countries.&nbsp;</font></p>
