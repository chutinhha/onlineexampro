</font><b><font face="Verdana" size="4">Redefining Mobile Telephony <br>
</font></b><font face="Verdana" size="1">
<b>Oman can now access Blackberry Enterprise, the leading wireless solution for 
connecting mobile users to customers <br>
</b><br>
<img border="0" src="news1.jpg" width="250" height="270" align="left" hspace="8" vspace="6">Oman Mobile has launched Blackberry wireless solution in the Sultanate. The 
initiative is to provide its customers a secure and integrated wireless 
communications solution. <br>
<br>
Blackberry Enterprise Server (BES) gives the user access to enterprise 
applications, including the CRM or ERP systems followed by the user’s company. A 
quad-band solution, BES gives coverage across the globe through Oman Mobile’s 
partner network. BES can be integrated easily with the existing systems and 
there is no need for synchronising with the office system. Blackberry users can 
send up to 10 emails. Emails and calendar appointments sent, received and 
deleted on Blackberry are also automatically updated and shown on the user’s PC. 
Messages can be tracked and accounted for from the office as well as from 
Blackberry anytime. As it is connected via GPRS and EDGE, the emails come to the 
device directly. Oman Mobile users can continue to use their SIM cards like 
before. All major email accounts can be created in this device. Browsing is fast 
as the mails are compressed 80 to 93 times. <br>
<br>
Blackberry features secure triple data encryption standard (DES) and advanced 
encryption standard (AES) to meet stringent business security guidelines. Each 
device is key board-locked and password protected. With a Blackberry enterprise 
server, if a device gets lost or stolen, the information it holds can be erased 
remotely, to avoid confidential personal or business information falling to the 
wrong hands. The users can send and receive emails and attachments, make 
calendar appointments and link to address book, all in real-time. <br>
<br>
Blackberry internet solution is basically for personal and small business use 
while enterprise solution is for corporate group use. Blackberry internet 
solution provides the users with the ability to have their personal or work 
email sent automatically to the Blackberry device from multiple internet service 
provider (ISP) email accounts. It can also provide the users with the ability to 
have their personal or work email sent from their Blackberry to their supported 
ISP email account. <br>
<br>
Users from multiple accounts can choose from which address to send an email. 
Ring tones, themes and wallpaper can be downloaded. Using the WAP browser, one 
can browse the internet. <br>
<br>
The Blackberry Enterprise solution is the leading wireless solution for 
connecting mobile users to customers. It helps increase productivity by giving 
employees real-time access to voice, email/personal information management, 
corporate data, and business applications. The employees can enjoy the benefits 
of being mobile and still have access to their corporate email, calendar 
contacts and more – all without lugging around a laptop or hunting for a phone 
jack. It also provides IT departments with simplified management and centralised 
control of wireless handhelds in a secure and flexible architecture. In the 
individual segment, the handset cost starts from RO239 and goes up to RO299 and 
the user has to pay RO10 in the first bill for the connection. Besides, there is 
a monthly charge of RO19.5. For the Blackberry enterprise solution, the server 
cost for one-time ranges from RO2,000 to RO9,000. Here too, there is a monthly 
cost of RO19.5 and RO10 for the connection. According to Mostafa Ahmed Kombor, 
sales manager - marketing and sales division, Oman Mobile, globally there are 
seven million Blackberry users as of 2006. From last year to this year, there 
has been an increase of 60 per cent and the user-base has now touched the 
11-million mark. Currently there are about 20 companies who are using Blackberry 
as a corporate connection. “Within six months, we target to increase it by 70 
connections. And by the end of 2008, we are aiming at increasing the data 
roaming facility in 100 countries,” said Mostafa.<br>
