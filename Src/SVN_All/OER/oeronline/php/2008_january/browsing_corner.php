        </font><b><font face="Verdana" size="4">Grand Sweep <br>
        </font></b><font face="Verdana" size="1"><b>Alan Greenspan’s memoirs is 
        a veritable reference book that provides insights into the global 
        economy and one that you will keep thumbing through in the days to come
        <br>
        </b><br>
        <img border="0" src="browse.jpg" width="200" height="189" align="left" hspace="8" vspace="6">Alan Greenspan needs no introduction for readers of this magazine. 
        Truly a gigantic global figure, whose actions as Chairman of the US 
        Federal Reserve for 20 long years (1987-2007) has had major 
        repercussions on the entire world. <br>
        <br>
        When such a personality puts pen to paper to walk down the memory lane, 
        the outcome cannot but be a best seller. That is what The Age of 
        Turbulence is, wherein the oracle speaks on life, money and politics.
        <br>
        <br>
        The only blemish in his illustrious career was his inability to call 
        correctly the 2000 dotcom bubble. Out of 36 substantive decisions he 
        made on the interest rates front, only one was wrong. An immaculate 
        performance, indeed! <br>
        <br>
        The book, written along with Peter Petre, consists of three parts: one 
        for ordinary folks who lap up autobiographies of celebrities/prominent 
        personalities. How many of us know about Greenspan’s jazz clarinet past? 
        How many of us know about Ayn Rand being his philosophical guru? All 
        these details are well captured in this tome. <br>
        <br>
        The second part is about his views on current events. It is pertinent to 
        note that the current subprime lending crisis that is gripping the 
        global financial market is believed to be Greenspan’s second major faux 
        pas. Critics point out that he misread the situation and led to the 
        present turmoil. <br>
        <br>
        The third part is about presidents and policy making in the US. 
        Greenspan has had the requisite insight into the thinking of not one, 
        but a slew of Presidents. Take a look at the list of those who depended 
        on his acumen to steer the US economy: Nixon, Ford, Reagan, Bush Sr., 
        Clinton and Bush Jr.. He could have stayed put for yet another term or 
        even more, because he was the darling of the occupants of White House 
        right from the day he ascended the Federal Reserve throne. But he chose 
        to step down to savour his future outside the Fed’s corridors.<br>
        <br>
        Greenspan’s tome is a veritable reference book that one will keep 
        thumbing through in the days to come not only in the world’s capitalism 
        citadel, but all across thanks to US’ superpower status and its impact 
        on citizens in every part of the globe. His analysis of the Black 
        Monday, the fall of the Berlin Wall, the rise of China and the collapse 
        of the erstwhile Soviet Union is perfect and impeccable.<br>
        <br>
        The Age of Turbulence is Greenspan’s version of his illustrious 60 years 
        of public life. Roping in Petre, a renowned autobiographist, is a master 
        stroke because he helps even lay readers make sense out of 
        Greenspanspeak, which otherwise would have gone over one’s head. A 
        memory journey worth tracking, though you don’t have to agree with 
        everything that the former Fed guru says. What are you waiting for? Grab 
        one at the Turtles right now.</font></p>
