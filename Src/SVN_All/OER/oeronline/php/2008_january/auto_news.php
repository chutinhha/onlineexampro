</font><font face="Verdana" size="2" color="#CC3300"><b>Mégane F1 Team R26 on 
top</b></font><font face="Verdana" size="1"><br>
<img border="0" src="auto5.jpg" align="right" hspace="8" vspace="6">French motor sport magazine, Echappement, has singled out the Mégane F1 Team R26 
as the winner of its much-coveted Sportive de l’année (Sports Model of the Year) 
award. This is for a record fifth time that Renault has lifted this highly 
prized accolade, introduced in 1982. Previous Renault winners were the Renault 5 
GT Turbo in 1985, Clio 16S in 1991, the Spider Renault Sport in 1996 and Clio 
Renault Sport in 2000. Developed by Renault Sport Technologies, Mégane F1 Team 
R26 is the sportiest member of the Mégane Renault Sport family. The purpose of 
the award and test procedure was to single out the car with the most suitable 
credentials. Factors considered were the vehicles’ performance, of course, their 
price and the general philosophy that underpinned them. <br>
