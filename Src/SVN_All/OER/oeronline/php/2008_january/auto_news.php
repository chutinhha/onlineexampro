        fully integrated centre of its kind for the car industry in Spain. This 
        building, located within Seat’s Technical Center, is the most modern 
        innovation complex in the car manufacturing sector to be found anywhere 
        in the world, enabling the company to fully harness synergies in R&amp;D in 
        order to provide design, one of the main defining features of the 
        Spanish brand’s models, with greater momentum. During the official 
        opening ceremony, Seat’s Chairman Erich Schmitt highlighted the 
        importance of the design centre vis-à-vis the investment it meant for 
        the future f the company. “This building is one of the many examples of 
        our commitment to quality, since the Seat products of tomorrow come from 
        ideas generated in this building today, products which will take the 
        brand towards sustainable growth until reach 800,000 units in 2018,” he 
        stated. In Oman, Towell Auto Centre is the authorised dealer of Seat.
