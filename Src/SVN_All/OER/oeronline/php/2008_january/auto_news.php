        in the Middle East”. According to Philip Moore, Publishing Director of 
        Middle East Car magazine, one of the judges, “It’s nice when you can 
        give an award to a vehicle that isn’t on the strength of it being cute 
        and groovy. When the automaker says it pays homage to the Jeep name, it 
        is perfectly true. Being functional, proud, and rough and tough as they 
        come, counts for plenty out there in the marketplace and it counted with 
        the judging panel. It’s excellent on the road; brutal and awesome off 
        it. The Commander has a good engine, and the build quality is 
        outstanding which impressed the judges a great deal. There wasn’t a lot 
        of debate over this one!”<br>
