<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
</font><b><font face="Verdana" size="4">More Golf, More Excitement </font></b><font face="Verdana" size="1">
<br>
<img border="0" src="golf1.jpg" width="175" height="123" align="right" hspace="6" vspace="4">With more than 40,000 golf courses across the world, golf has emerged as one of 
the most popular sport and not-to-be-missed networking opportunity for the 
businessmen and top executives. Golf is getting popular in Oman as well as the 
number of golfers is increasing every year and more golf courses are on their 
way. <br>
<br>
Taking the excitement of playing golf to a new high, the 4th OER CEO Golf 2008 
will tee off at the Ghallah Wentworth Golf Club on February 21. Since its 
conception in 2005, OER CEO Golf has been a pioneering initiative from Oman 
Economic Review aimed at promoting Golf in Oman. The success of OER CEO Golf in 
2005, 2006 and 2007 witnessed an elite gathering of industry leaders come 
together for the Sultanate’s biggest networking event and golf experience. <br>
<br>
OER CEO Golf 2008 will be an 18-hole informal yet innovative Golf tournament. It 
will bring together the big drivers of the business world along with industry 
barons from Dubai, Bahrain, Kuwait and Qatar broadening the number of golfers 
and giving the Oman business heads an opportunity to network with their peers 
from across the Gulf. The representatives from across industries, regular 
golfers and non-golfers will be in action on the turf and at the ever popular 
golf clinic and 5-hole golf round for non-golfers. <br>
<br>
<img border="0" src="golf2.jpg" width="175" height="263" align="left" hspace="8" vspace="6">With the 2008 Edition, the scope of the golf tournament has doubled. At OER CEO 
Golf 2008, you will witness more excitement and more enthusiasm with the 
continuation of the successful “Golf Clinic” and the addition of a new feature 
“Virtual Golf Simulator”, proof of the event organizer’s continued endeavor to 
enhance the OER CEO Golf experience. <br>
<br>
OER CEO Golf 2008 promises to be an event of substantial stature like the past 
three years in terms of its gathering, profile of participants and guests, 
prizes galore and organization and flow of the event. Like the very nature of 
Golf, this event serves as the perfect platform for developing and strengthening 
current and future business relations as the participants and guests spend the 
evening interacting and networking with their peers and fellow participants over 
music, dance and sumptuous food. To ensure that the non-golfers are not left out 
of the golfing circuit, a Golf Clinic has been designed, wherein professional 
golfers will train and educate the non-golfing participants about golf, 
ensuring, while many may not convert into avid golfers, at least every 
participant whether golfer or non-golfer walks home, more equipped and informed 
about the sport. Besides, they will have a special experience of a 5-hole round 
of golf. <br>
<br>
The itinerary of the CEO GOLF 2008 boasts of innovative yet exciting golfing 
concepts like the “Virtual Golf Simulator” in the evening party apart from 
serious golf during the day. Indeed this OER CEO Golf will be an event to 
remember and leave a lasting memory on the attendees since it has succeeded in 
adding glitz and a character to the sport. <br>
<br>
The ideal concluding format for the event would have to be an evening of the 
gala dinner and dance to felicitate the winners, give away the fantastic and 
extravagant prizes and network with the crème de la crème of the Corporate Oman. 
Indeed OER CEO Golf 2008 has all the right ingredients to be proclaimed the 
finest networking event of Oman alongside being tied up with the finest 
corporate sport – Golf and the most high profile set of partners to take the 
event to new heights.</font><p align="center" style="margin: 3px 10px">
