<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
</font><b><font face="Verdana" size="4">Dubai set for Desert Classic 2008
</font></b><font face="Verdana" size="1"><br>
<b>Tiger Woods, who came third in the 2007 edition, will be back to compete for 
the top spot in the Dubai Desert Classic </b><br>
<br>
<img border="0" src="golf.jpg" width="250" height="253" align="left" hspace="8" vspace="6">Come January end, Dubai will host the world’s leading golfers, who will vie for 
the coveted Desert Classic trophy at the Emirates Golf Club. Tiger Woods has 
confirmed that he will be returning to the city to compete in the forthcoming 
2008 edition scheduled to be held from January 29 to February 3. Woods, who 
first appeared in the Desert Classic in 2001, finished tied third in the 2007 
edition, two shots adrift off eventual winner Henrik Stenson. Arguably one of 
the most remarkable athletes, Woods had his moments, but poor putting blew his 
chances. <br>
<br>
“Well, that’s one of the worst putting weeks I have had in a long time… very 
frustrating,” said Woods who, for a change, wore sunglasses during the 
tournament. Be sure that Tiger will be chomping at the bit to get off to a 
roaring start in Dubai. “I always enjoy visiting Dubai – it’s an extremely 
hospitable place where the people and the organisers treat the players so well,” 
he said. Woods has strong bonds with the Emirates and he along with his design 
company are developing a 7,800-yard, par-72 course called Al Ruwaya in Dubailand, 
the region’’s largest tourism and leisure project. <br>
<br>
A new chapter will be added to UAE golf history when Khalid Yousuf, the 
18-year-old prodigy, joins the elite field contesting for honours in the 2008 
Dubai Desert Classic. The UAE national champion has been handed a dream 
sponsor’s invite by ‘’Golf in Dubai’’ – the promoters and organisers of the mega 
European Tour event – in recognition of his meteoric rise not only on the UAE 
circuit, but in the Arab world as well. Khalid Yousuf, who will be the first UAE 
national ever to tee off in the Dubai Desert Classic since the inception of the 
championship in 1989, recently took the Pan Arab Junior Championship in Egypt by 
storm, winning the individual title and guiding the UAE National Team to an 
emphatic victory in the team event.
