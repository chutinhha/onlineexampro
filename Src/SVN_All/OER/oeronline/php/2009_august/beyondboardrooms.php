            like in India or even Dubai, and that it is easier for executives 
            here. Most of them don’t feel it really necessary to keep aware of 
            the developments to do well here. But Divecha believes in the 
            contrary. “Irrespective of where you are and what you do, if you do 
            something today and if you do the same thing tomorrow, you should be 
            able to do it a little better, quicker, cheaper, faster as the case 
            may be. You have to continuously improve yourself and you only stop 
            improving the day you die.” This is the philosophy by which Divecha 
            lives everyday, hopefully for the rest of his life.<br>
            <br>
            <br>
            <b>Mixing business with pleasure</b><br>
            His basic interest is to watch movies and business programmes on 
            television, such as CNBC, Fox business, Bloomberg, BBC. It is the 
            passion of trying to always be on top of things and the desire to be 
            aware of what is happening in this fast changing world. “I enjoy 
            keeping in touch with developments internationally that helps me 
            professionally and also helps me in my personal investments.” He 
            also believes that living in Oman gives one access to a wealth of 
            information which you very rarely find in India or America or even 
            in the UK. This is because, in Oman, you can find a huge variety of 
            programmes through the satellite that is difficult to come by 
            elsewhere.<br>
            <br>
            Divecha, an avid traveller, has already visited numerous countries. 
            But despite having travelled extensively, he doesn’t have a 
            particular destination he calls ‘his favourite’. “I always end up 
            mixing business with pleasure. Even when I am on a holiday, if there 
            is a business opportunity I take time out to pursue it, as not doing 
            so could result in regrets later on. While my wife calls me a 
            workaholic I call it being opportunistic. As people say, ‘If you 
            snooze, you lose” he quipped. And considering that he has been in 
            Oman for nearly 30 years, there isn’t a part of Oman he hasn’t seen. 
            This is because, to keep relationships strong, he is always out with 
            business colleagues who are also friends to him and his family. In 
            fact, most of his trips in Oman and abroad were with friends who 
            started off as business acquaintances and colleagues.<br>
            <br>
            Divecha believes that it is important to never focus on the 
            negatives of a situation or person but instead on the positives. 
