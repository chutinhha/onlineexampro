            professional life, who form the core management team. ‘‘And with 
            such a great team of talented people and the support of a visionary 
            Chairman, our Company can only prosper and grow further.” We call 
            ourselves the “OHI family” and all of us are proud to be a part of 
            this very dynamic family.</font></p>            
