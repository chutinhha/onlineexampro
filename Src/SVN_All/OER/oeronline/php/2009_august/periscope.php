                    <br>
                    Dollar interest rates are mission critical for GCC investors. 
                    Since all GCC currencies (except Kuwait) are pegged to the 
                    dollar, regional central banks must shadow the monetary policy 
                    initiated by the Federal Reserve, even though the business 
                    cycles in America and the Gulf are not synchronised. This 
                    means that the GCC imports both inflation (as in 2006-07 when 
                    the dollar plunged) and deflation (as after September 2008, 
                    when Lehman Brothers went bankrupt and the regional property 
                    market collapsed). Since the GCC cannot devalue, financial 
                    shocks that emanate from Wall Street have a devastating impact 
                    on local asset markets, as happened after the tech bubble 
                    popped in Silicon Valley in 2001, after oil prices collapsed 
                    in the late 1990’s or the credit tsunami gutted the international 
                    banking system in 2008-2009. It is imperative that savers, 
                    investors, homeowners, bankers and depositors in the GCC understand 
                    the nuances and risk calculus of US monetary policy and the 
                    term structures of dollar interest rates. The price of money 
                    in America, after all, determines the planet’s cost of capital.<br>
                    <br>
                    The June Federal Open Market Committee (FOMC) made it clear 
                    that the Ben Bernanke Fed is no longer worried about deflation 
                    risks and financial Armageddon. Helicopter Bernanke even issued 
                    (an entirely unconvincing) bromide against inflation risks, 
                    though he refused to accelerate the central bank’s Treasury 
                    bond purchase programme or discuss hawkish exit strategies 
                    for his unorthodox monetary policies (an euphemism for the 
                    money printing spree known as quant easing or QE). Yet the 
                    Federal Reserve Board made sure that it indented to keep its 
                    overnight borrowing rate at near zero for an “extended period 
                    of time.”<br>
                    <br>
                    <b>Ominous signals</b><br>
                    The ISM, retail sales and property market data suggest that 
                    the US economy is near a bottom, though the jobless rate is 
                    9.5 per cent and rising. The shock decline of 467,000 jobs 
                    in June proved that the path to recovery is not linear. It 
                    is difficult to have a vibrant consumer economy with 14mn 
                    jobless Americans, a surge in gasoline prices and a six trillion 
                    dollar neutron bomb in household net worth when the housing 
                    bubble went bust and the grizzlies ran amok in the stock market. 
                    US Treasury bond rates are determined by supply, demand, inflation 
                    expectations and perceptions of risks in the capital markets. 
                    Long term dollar are influenced, not determined, by the Federal 
                    Reserve. It is ironic that US money markets price a November 
                    FOMC tightening that will simply not happen, if for no other 
                    reason than a Democratic White House, Congress and Senate 
                    make it political suicide for Chairman Bernanke to hike rates 
                    when the jobless rate is still rising. So even though the 
                    financial markets are terrified about the $2trn budget deficits 
                    of Obamanomics, I doubt if the US Treasury note yield rises 
                    much above four per cent, even if inflation expectations rise 
                    once again this summer. The Fed has done its best (risked 
                    its political independence, midwifed Wall Street shotgun marriages 
                    for Merrill Lynch and Bear Stearns acted as lender, insurer 
                    and buyer of the last resort, tripled its balance sheet, slashed 
                    its policy rate to zero) to act as the white knight for the 
                    world financial system. It will not countenance a bloodbath 
                    in the bond market that would lead to a catastrophic spike 
                    in US mortgage rates or consumer loans, the lubricants for 
                    an income short, debt burdened, shell shocked US consumer. 
                    The Bernanke Fed will talk tough on inflation but remain ultra-dovish 
                    in its monetary policy.<br>
                    <br>
                    <b>Remedial measures </b><br>
                    The June nonfarm payrolls demonstrate that US consumer spending 
                    capex and export will remain weak, as the credit crunch/balance 
                    sheet recession of the early 1990’s. This was followed by 
                    a sell off in global risk assets such as equities, crude oil, 
                    gold and emerging markets. US Treasury bond yields and 10 
                    year interest rate swaps dropped dramatically in July. While 
                    the dramatic spread compression in US corporate/high yield 
                    debt suggests that the bond market has priced out deflation 
                    risk, US consumer woes and bank loses could still hit global 
                    GDP growth optimists in the next three months. Since the Fed 
                    made it clear that it will not dump trillions of dollars from 
                    its “cash for trash” bank/commercial paper/mortgage debt, 
                    Treasury note yields will not rise dramatically as long as 
                    Bernanke signals that he will not exit Quantitative Easing 
                    (QE). Still, Obamanomics will lead to the heaviest Uncle Sam 
                    IOU issuance in the history of the American Republic. This 
                    means banks will borrow short at near zero rates, invest surplus 
                    capital in US Treasury. Meanwhile, California, New York and 
                    Florida home prices are still falling. This means more black 
                    swans in American bank balance sheets. This is the reason 
                    that while the endgame for the debt market is still El Grizzly 
                    Supremo, I believe the ten year T-note yield could fall to 
                    three per cent this summer as the stock market rally dies 
                    and risk overwhelms the animal spirits of the greenshooters. 
                    The best bet on a bullish US Treasury note market is the index 
                    fund TLF, a proxy for long duration American government bonds.<br>
                    <br>
                    The shock waves of a Kuwaiti investment bank’s default on 
                    a public sukuk has ominous implications for the global market 
                    for Sharia compliant debt. Both Moody’s and Standard and Poor’s 
                    have downgraded the credit ratings of several prominent Gulf 
                    sukuk issuers. Moreover, several sukuk deals were sold via 
                    structured investment vehicles and sukuk certificate holders 
                    could well find themselves unsecured creditors without a claim 
                    on the issuer’s assets. Moreover, courts in the Gulf have 
                    no experience in restructuring sukuk issues in defaults. The 
                    $15bn debt shock from Saudi Arabia’s Saad/Gosaibi also proves 
                    that name lending could prove to be the collateral debt obligation 
                    (CDO) time bomb of gulf banking.<br>
