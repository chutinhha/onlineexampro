                2009.<br>
                <br>
                <b>GCC markets</b><br>
                All the markets ended in red in the month ended July 15, 2009. 
                Dubai and Doha markets fell the most while all other markets 
                fell more than five per cent. Most of the markets tracked global 
                cues and oil prices. Saudi markets were driven down by 
                petrochemical stocks following subdued crude oil prices amid 
                continuing worries about the global economy. Despite the fall, 
                Saudi market still remains the best performing market in the GCC. 
