                the same period to RO9.2mn. Overall the market saw a sell off 
                with an advance decline ratio of 1:3. In corporate news, Oman 
                National Engineering and Investment Company was awarded a 
                contract from Petroleum Development of Oman, worth RO3.6mn for a 
