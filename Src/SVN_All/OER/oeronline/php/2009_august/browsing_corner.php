                  <p style="margin: 5"><b><font face="Verdana" size="4"><br>
                    Cracking the dilemma<br>
                    </font></b><font face="Verdana" size="1"><strong>The author 
                  raises pertinent issues regarding the prevalence of poverty in 
                  this thought provoking and well researched book<br>
                  </strong> 
                    <br>
                    William Easterly’s The White Man’s Burden is about what its 
                  author calls the twin tragedies of global poverty. The
                  <img src="images/img_book.jpg" width="187" height="319" hspace="9" vspace="5" align="right">first, 
                  of course, is that so many are seemingly fated to live 
                  horribly stunted, miserable lives and die 
                  such early deaths. The second is that after fifty years and 
                  more than $2.3 trillion in aid from the West to address the 
                  first tragedy, it has shockingly little to show for it. We’ll 
                  never solve the first tragedy, Easterly argues, unless we 
                  figure out the second. <br>
                  <br>
                  <b>Beyond the obvious</b><br>
                  The ironies are many: We preach a gospel of freedom and 
                  individual accountability, yet we intrude in the inner 
                  workings of other countries through bloated aid bureaucracies 
                  like the International Monetary Fund (IMF) and World Bank that 
                  are accountable to no one for the effects of their 
                  prescriptions. We take credit for the economic success stories 
                  of the last fifty years, like South Korea and Taiwan, when in 
                  fact we deserve very little. However, we reject all 
                  accountability for pouring more than half a trillion dollars 
                  into Africa and other regions and trying one “big new idea” 
                  after another, to no avail. Most of the places in which we’ve 
                  meddled are in fact no better off or are even worse off than 
                  they were before. Could it be that we don’t know as much as we 
                  think we do about the magic spells that will open the door to 
                  the road to wealth? <br>
                  <br>
                  Absolutely, says the author in this angry, irreverent, and 
                  important book. He contrasts two approaches: one the 
                  ineffective planners’ approach to development - never able to 
                  marshal enough knowledge or motivation to get the 
                  overambitious plans implemented to attain the plan’s arbitrary 
                  targets and two a more constructive searchers’ approach - 
                  always on the lookout for piecemeal improvements to poor 
                  peoples’ well-being, with a system to get more aid resources 
                  to those who find things that work. Once we shift power and 
                  money from planners to searchers, there’s much we can do 
                  that’s focused and pragmatic to improve the lot of millions, 
                  such as public health, sanitation, education, roads, and 
                  nutrition initiatives. We need to face our own history of 
                  ineptitude and learn our lessons, especially at a time when 
                  the question of our ability to “build democracy,” to 
                  transplant the institutions of our civil society into foreign 
                  soil so that they take root, has become one of the most 
                  pressing we face.<br>
                  <br>
                  “The White Man’s Burden,” is an important book. Easterly asks 
                  the right questions, combining compassion with clear-eyed 
                  empiricism and people should heed what he has to say.<br>
