		        <td width="100%" valign="top"> <p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
                    </font><b><font face="Verdana" size="4">Facts and 
                perceptions on workers in GCC</font></b><font face="Verdana" size="1">&nbsp; <b>
                <img border="0" src="images/img55.jpg" align="right" hspace="5" vspace="5" width="120" height="156"></b><font face="Verdana" size="2" color="#CC3300"><b><br>
                </b></font><b>Senior executives hold different and in some cases 
                extreme views regarding local and foreign workforce in Gulf 
                Cooperation Council (GCC) countries</b><font face="Verdana" size="2" color="#CC3300"><b><br>
                </b></font> 
                    <br>
                  Some of these challenges were debated by executives in a 
                recent report issued by PricewaterhouseCoopers for Mohammed bin 
                Rashid Al Maktoum Foundation. Sheikh Mohammed bin Rashid, in 
                turn Vice President and Prime Minister of the UAE and ruler of 
                Dubai stands behind the extraordinary project. Sheikh Mohammed 
                launched the foundation during the World Economic Forum in 
                Jordan in May 2007. <br>
                <br>
                With a sizable fund of US$10bn, the foundation is one of the 
                largest charitable entities in the world. Amongst others, the 
                entity aims at bridging the knowledge gap between the Arab 
                region and the developed world by improving the standard of 
                education and research in the region, developing leadership 
                programmes for youth, and stimulating job creation. <br>
                <br>
                <b>Alarming demographics</b><br>
                The report po<img border="0" src="images/img56.jpg" align="left" hspace="6" vspace="5" width="175" height="282">ints out to alarming statistics about the region’s 
                demographics. The document suggests that some 80 per cent of GCC 
                nationals are below 25 years of age. This phenomenon reflects 
                extraordinary population growth rates over the past two decades, 
                exceeding five per cent per annum in several GCC nations. 
                However, population growth rates now averages around three per 
                cent.<br>
                <br>
                Nevertheless, the statistics indicate availability of local 
                workforce in the years ahead. Most youths seek employment after 
                graduation rather than depend on their parents for their 
                financial well-being. Some elect to attain higher degrees 
                notably professional certificates while working. Education is 
                viewed as a gateway to gaining best possible employment 
                opportunities.<br>
                <br>
                On the other, finding suitable positions for new entrants in the 
                job market is a daunting challenge. The majority of locals 
                prefer working in government departments or more appealing 
                positions notably the financial services sector. In other words, 
                labour authorities cannot overlook the fact that the challenge 
                relates to finding suitable employment positions for locals and 
                certainly not just creating many job opportunities.<br>
                <br>
                <b>High expectations</b><br>
                GCC nationals tend to have high expectations of jobs, pay and 
                working conditions. Undoubtedly, at stake is not creating 
                substantial employment opportunities per se, but rather the 
                right ones for locals. GCC nationals do not want jobs that 
                provide relatively low wages or lack attractive working 
                conditions such as the construction sector. The issue is not 
                whether this is right or wrong, but a fact of life for GCC 
                nationals. Certainly, such attitudes could change in the long 
                run through rehabilitation and training. Thus, the challenge 
                relates to ensuring availability of jobs that meet the 
                requirements of nationals.<br>
                <br>
                Not surprisingly, foreign nationals tend to soak up majority of 
                fresh employment opportunities in all GCC countries with no 
                exception. For instance, foreign nationals obtained 96 per cent 
                of all fresh employment opportunities offered by private sector 
                firms in Bahrain in 2008. The utmost majority of these new job 
                opportunities were in the construction sector, which is noted 
                for not being so attractive as far as pay and working conditions 
                are concerned. To be sure, foreign nationals comprised 76 per 
                cent or 453,582 of 596,224 strong workforce in the first quarter 
                of 2009.<br>
                <br>
                <b>Not so ready </b><br>
                The most revealing part of the report relates to perceptions 
                held by decision makers regarding sufficient supply of qualified 
                local workers. For example, merely 12 per cent of decision 
                makers of firms operating<img border="0" src="images/img57.jpg" align="right" hspace="6" vspace="5" width="200" height="510"> in Bahrain believe that there is a 
                sufficient supply of local workers. Other respondents put the 
                figures in the range of 14 per cent in the UAE and as high as 29 
                per cent in Kuwait.<br>
                <br>
                Astonishingly, 91 per cent of executives depend on foreign 
                nationals to fill in their employment needs. The issue points 
                towards the need to ensure availability of qualified local 
                workers with regards to education and training. Clearly, GCC 
                economies are capable of creating job opportunities, in turn a 
                sign of healthy economic growth rates. <br>
                <br>
                Still, another astonishing disclosure concerns views of top 
                executives with respect to both local and non-local managers. 
                The report says that that approval rates of local and 
                international executives range from 68 per cent to 90 per cent, 
                respectively. <br>
                <br>
                Yet, the figures become only more of a cause of concern with 
                regards to middle-management personnel. Some 92 per cent of top 
                executives happen to be pleased with quality of work performed 
                by foreign nationals holding middle-management positions. By 
                comparison, merely 55 per cent of executives approve work 
                carried out by local middle managers.<br>
                <br>
                Clearly, local managers have a daunting task overcoming adverse 
                perceptions held by top decision makers. Undoubtedly, 
                perceptions of decisions makers matter, as they are ones who 
                decide about employment, assessment and promotion.<br>
                <br>
                <b>Era of women</b><br>
                The report points out to the phenomenon of women joining the 
                workforce throughout the GCC states in greater numbers. The 
                change can be related to socio-economic changes occurring in the 
                region, with females looking for financial security. A number of 
                them work in social service sectors such as education and 
                health. No statistics are available to support the claim, but 
                possibly there is little to dispute this trend.<br>
                However, the report conveniently overlooks discussing 
                discriminatory practices against females for mere biological 
                matters like pregnancy. It is alleged that some employers only 
                solicit bio-data from prospective male job seekers. Not 
                surprisingly, females comprise some 82 per cent of jobless in 
                Bahrain. This is true of Bahrain, a country widely appreciated 
                for its liberal tendencies, meaning that the situation could 
                only be worse in more conservative GCC countries.<br>
                <br>
                In conclusion, job markets in GCC are likely to experience 
                fundamental changes in the not too distant future. Already, 
                Bahrain has decided to grant foreign workers the right to change 
                sponsors, as per article No. 25 of the Labour Law. Certainly, 
                change is the order of the day with regards to regional job 
                markets.<br>
