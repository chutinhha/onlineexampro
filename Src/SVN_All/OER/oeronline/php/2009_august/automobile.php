                <br>
                <b><font face="Verdana" size="4">Selling with a difference</font></b><font face="Verdana" size="1"><img border="0" src="file://192.151.106.252/wwwroot/majorsites/oeronline/php/2009_august/images/img15.jpg" align="right" vspace="5" hspace="8" width="150" height="202">&nbsp; <br>
                    Malcolm Xavier Crasta caught up with Makoto Ono at the 
                recent launch of Bridgestone’s B250 tyre to chat about the 
                current standing of Bridgestone in the world.<br>
                <br>
                Makoto Ono joined Bridgestone in 1982, straight out of college. 
                He has been involved mostly in international appointments 
                including Mexico, US, Jordan, UAE, South Africa and currently 
                Saudi Arabia. He is currently the director of sales and consumer 
                tyre marketing, Bridgestone Middle East and Africa.<br>
                <br>
                <br>
                <b>How does Bridgestone stand in comparison to the rest of the 
                market globally?</b><br>
                There is a bi-polarisation in the global tyre industry. First, 
                we have the traditional ‘premium brands’ in the high-end and 
                then we have the new ‘entry-level’ manufacturers, namely from 
                China and South-East Asia. This is now the fastest growing 
                segment globally and all the while the middle ground is vastly 
                eroding. As such, if we continue to sell in the way we have 
                been, we will fast run out of resources to grow further. So, 
                what we are now trying to emphasise in our sales is to have 
                ‘value added’ in terms of services and products to at least 
                differentiate ourselves. <br>
                <br>
                The advantage we have over our competitors is pure scale. There 
                are two key players in the industry, Bridgestone and Michelin. 
                Aside from these there are other well known manufacturers such 
                as Goodyear, Continental and Pirelli. I think, out of all these 
                brands Bridgestone is probably prepared best to develop a new 
                business model to differentiate ourselves from the rest of the 
                field. One such example is the fact that we currently emphasise 
                solution business. Let’s consider bus and truck tyres. Nowadays, 
                we have to come away from just selling a new tyre. We have just 
                purchased a new rethread company called Bandag and we hope to 
                develop a new business model where we offer the selling of a new 
                tyre, rethread and the service. <br>
                <br>
                When it comes to consumer products, we have just introduced the 
                new B250 into the Omani market. We do not just want to sell a 
                new product, but we also now emphasise developing a sales 
                network. There are many things on the horizon and we are now 
                thinking forward in ways to differentiate ourselves from our 
                competition in a way that they cannot easily duplicate.<br>
                <br>
                <br>
                <b>How would you differentiate your new tyre, the B250, from the 
                competition?</b><br>
                There are various parameters that a tyre is judged by such as 
                wet performance, braking performance, riding comfort, noise 
                levels and so forth. One of the most unique aspects of the tyre 
                when we developed it was the fact that we have managed to 
                maintain the levels of each of these parameters but have also 
                managed to decrease the rolling resistance of the tyre. In 
                comparison to its predecessor, the coefficient of the rolling 
                resistance has been decreased by close to ten per cent. This 
                means better fuel consumption and the fact that it is more 
                environmentally friendly.<br>
                <br>
                The B250 is catered to the mass segment. We have sold, since its 
                introduction in 2004, over 12mn tyres and we now sell the tyre 
                in 95 countries. I think this says a lot about the reliability 
                and quality of the tyre. <br>
                <br>
                <br>
                <b>In light of the financial crisis, the auto industry has been 
                one of the hardest hit. How has this affected Bridgestone?</b><br>
                Our latest projections for the 2009 sales, which was released in 
                May, sees our net sales declining by 22 per cent in 2009 versus 
                2008. Our operating profits will decrease by 38 per cent. What 
                we are seeing is that, the costs of producing a tyre as a 
                product is heavily dependent on the pricing of oil because it is 
                an oil-based product. As such, our operating profits have eroded 
                very quickly. The way we intend to address this is to try and 
                find new business models. <b><br>
