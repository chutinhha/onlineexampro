                <img border="0" src="images/img42.jpg" align="right" hspace="6" vspace="2" width="175" height="234"><br>
                <b>Paolo De Renzis</b>, British Airways Area Commercial Manager, 
                Middle East replies to a set of questions sent by OER regarding 
                the Airlines summer schedule, the financial crisis and its 
                impact<br>
                <br>
                <br>
                <b>British Airways summer schedule sees a 35 per cent increase 
                in frequencies to the GCC region. What is the idea behind this 
                increase in frequencies?</b><br>
                The aviation industry is experiencing the worst trading 
                environment it has ever faced and no-one is immune from the 
                global economic downturn, not even the Middle East. That said, 
                although the region may not be enjoying the spectacular growth 
                it has seen in recent years, it remains a vitally important 
                market to the airline and one where we are continually examining 
                expansion strategies.<br>
                <br>
                <br>
                <b>How important is the re-entrance of British Airways into 
                Saudi Arabia’s market?</b><br>
                The 2005 suspension of our Saudi services was a purely 
                commercial decision; the recent development and investment into 
                the Kingdom makes us believe flights to Saudi Arabia can once 
                again be profitable. Our re-entry into the Kingdom of Saudi 
                Arabia, which will see British Airways operate five weekly 
                flights to stations in both Jeddah and Riyadh, represents a 
                ten-flight contribution to what is our largest-ever Middle East 
                summer schedule. However, Saudi Arabia is an important market 
                for British Airways and to ensure profitability we are keen to 
                deliver high levels of service we know our Saudi customers 
                expect.<br>
                <br>
                <br>
                <b>What makes the airline confident about the Oman market?</b><br>
                Backed by the Oman Mini<img border="0" src="images/img43.jpg" align="left" hspace="7" vspace="4" width="200" height="170">stry of Tourism, the country is investing 
                heavily in both its short and long term tourism strategies. Oman 
                is developing attractive tourism propositions which are tailored 
                towards leveraging its heritage, adventure and luxury offerings 
                and amazing natural assets, which include stunning sea fronts, 
                mountains and vast green areas. And it’s proving successful. 
                Oman attracts three million tourists a year, and its major 
                cities such as Matrah, Barka and Salalah recorded some of the 
                region’s highest growth in city arrivals for 2007-2008. The 
                success is further strengthened by the fact the average spend 
                per visitor is $1,077 – double that of the UAE. <br>
                <br>
                When you also consider the historical links between Oman and the 
                UK, which are well known, and the fact that the UK is one of the 
                country’s main source markets in terms of inbound tourists, I 
                think it is very clear why British Airways is confident about 
                Oman. The Omani market remains an important market for us and 
                opportunities to increase our domestic marketshare are always 
                under consideration. <br>
                <br>
                <br>
                <b>British Airways expects to make a loss of RO83.3mn this year? 
                What accounts for the fall in profits?</b><br>
                Globally, the general economic downturn has dented consumer 
                confidence and led to a decline in demand for travel. Last year, 
                when record fuel process further impacted the airline 
                environment, we saw over thirty airlines go out of business and 
                more will possibly follow this year. Trading conditions are 
                challenging; long-haul premium traffic numbers are softening and 
                forward bookings are being affected globally – this all affects 
                profits.<br>
                <br>
                <br>
                <b>What is the airline’s strategy to negotiate the slowdown? </b>
                <br>
                We are looking at consolidation in many of our markets and the 
                proposed Iberia merger, for example, would enable us to work 
                more effectively together in our traditional operational 
                markets, become more efficient by synergising revenues and costs 
                and allow us to compete more effectively around the world. Aside 
                from that, I firmly believe with our competitive fares, 
                award-winning products and services and global network of more 
                than 150 destinations, that British Airways - via our 
                £4.3billion Terminal 5 hub at London Heathrow – will remain 
                commercially competitive in the long-term. <br>
