		        <td width="100%" valign="top"> <p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
                    </font> <b><font face="Verdana" size="4">A complete makeover</font></b><font face="Verdana" size="1"><strong><br>
                    Shabir Al Yousef, CEO, Oman Investment &amp; Finance Company 
                    speaks to Mayank Singh about rebranding, challenges and new 
                    business opportunities </strong><strong><br>
                    </strong><strong style="font-weight: 400"><br>
                    </strong><strong>What was the rationale of changing the OIFC 
                    logo? </strong><strong style="font-weight: 400"><br>
                    We decided to create a new ‘Logo’ to clearly and more effectively 
                    convey our true identity. If you look at the new logo, it 
                    is an embodiment</strong><strong style="font-weight: 400"> 
                    of all our value<img border="0" src="images/img5753.jpg" align="right" hspace="7" vspace="7" width="296" height="210">s 
                    and our future direction, i.e. ‘shelter’, ‘protection’, ‘hierarchy’, 
                    ‘new horizons’ and the ‘way forward.’ It also portrays a shield 
                    that depicts our history. We will be completing 30 years of 
                    ‘being in business’ in December 2009. As there are many dimensions 
                    to the different services we provide, it is our endeavour, 
                    to be perceived as a multi-business and services company.<br>
                    <br>
                    </strong><strong>What are the various services that OIFC offers 
                    to its clients?</strong><strong style="font-weight: 400"><br>
                    We, as an agent for the Public Authority for Electricity &amp; 
                    Water, Oman Wastewater Services (Haya), Muscat Electricity 
                    Distribution Co., Oman Telecommunications Company and Oman 
                    Mobile, render the following services -- meter reading, billing 
                    and collection services for electricity and water utilities 
                    and bill printing, mailing and collection and debt factoring 
                    services for all telecom utilities (fixed line telephone, 
                    GSM and Internet - both dial up and ADSL). These work as the 
                    lifeline of the people. <br>
                    <br>
                    We have always been striving to enhance our service levels 
                    to both our principals and valued customers. The working hours 
                    of our main branches, all over the Sultanate of Oman, has 
                    been extended by about two hours, from 24th of every month 
                    to 2nd of the subsequent month, which is considered as a ‘peak 
                    period’ for the settlement of bills. We have recently launched 
                    our website which helps customers to print all utility bills. 
                    Another initiative is the ‘OIFC Corporate Club’ that we have 
                    recently introduced and which caters exclusively to the needs 
                    of corporate customers, viz. banks, companies, embassies etc. 
                    The corporate cell serves as a ‘one-stop’ shop for sending 
                    bills through E-mail, various payment options and for all 
                    related services. We are also in the process of refurbishing 
                    our branches bringing them to a high standard to cater to 
                    the demands of our customers. With the recently opened branch 
                    in Muttrah, we have, today, 45 branches, spread all over the 
                    Sultanate of Oman. Another addition to the list of services 
                    provided by us, is the selling and activation of prepaid cards, 
                    as one of the master distributors for Oman Mobile. We have 
                    tied up with couple of banks to offer additional payment options 
                    to our valued customers, whereby a customer can pay the utility 
                    bills directly through their accounts. We are also looking 
                    at the options of introducing our own ‘Bill Payment Machines’ 
                    (BPMs) which will serve as an additional payment mode for 
                    our customers. <br>
                    <br>
                    </strong><strong>Why was the need felt for restructuring the 
                    Company?</strong><strong style="font-weight: 400"><br>
                    We have restructured OIFC into different ‘business oriented 
                    units.’ We have segregated our operations into different business 
                    units, depending on their functionalities, each business unit 
                    has a dedicated team and resources. For instance, the electricity 
                    and water business unit has a dedicated team, headed by a 
                    general manager. So is the telecom operations. Each business 
                    unit has its own ‘Key Performance Indicators’ (KPIs) with 
                    suitable incentives which we hope, will help in reducing costs, 
                    motivating staff, increasing efficiency and in creating profit 
                    centres. Although we have been in business for the last three 
                    decades in order to create better awareness about our company 
                    and our activities, a new department for ‘branding and marketing’ 
                    has been created.<br>
                    <br>
                    </strong><strong>What are the challenges that you face in 
                    your business?</strong><strong style="font-weight: 400"><br>
                    We can take the meter reading operations for example. It is 
                    completely a manual and tedious process. We have introduced 
                    a hand-held, automated, meter reading device (PDA) which has 
                    not only simplified the job but also helps in accurate meter 
                    readings, reducing errors involved in the manual process. 
                    Meter readings are taken by reading the bar code labels fixed 
                    on the electricity and water meters. The main problem we face 
                    is access to the premises for meter reading, as sometimes 
                    the occupants of a premise are not available. Also, sometimes 
                    we find that bar code labels have been removed, without realising 
                    the fact that meter readings cannot be taken without these 
                    bar codes which affects the overall consumption and billing. 
                    <br>
                    <br>
                    Another challenge which we face is the large number of customers 
                    turnout at our branches during the last week of the month 
                    and the rush continues till the first week of the next month. 
                    As mentioned earlier, this is a peak period for us as it is 
                    the salary time for most people and there is a heavy rush 
                    for payment of utility bills. During this period we keep our 
                    branches open for extra two hours in the evening. We are looking 
                    at different options to upgrade our services for the convenience 
                    of our custom</strong><strong style="font-weight: 400">ers, 
                    to reduce the rush.<br>
                    <br>
                    </strong><strong>How many customers does OIFC service every 
                    month?</strong><strong style="font-weight: 400"><br>
                    We service about 200,000 electri<img border="0" src="images/img37.jpg" align="left" hspace="8" vspace="7" width="350" height="311">city 
                    customers, 130,000 water customers in the capi</strong><strong style="font-weight: 400">tal 
                    area (Muscat governorate). In the telecom side, we service 
                    all the customers of Omantel and Oman Mobile. We hav</strong><strong style="font-weight: 400">e 
                    a dedicated team of abo</strong><strong style="font-weight: 400">ut 
                    650 employees, out of which approximately 300 staff are engaged 
                    in the electricity sector, 250 in water and the rest are in 
                    support services.<br>
                    <br>
                    </strong><strong>What is your business model and how does 
                    it operate?</strong><strong style="font-weight: 400"><br>
                    Actually our business model is a very unique and an interesting 
                    one, not easily found in other places. We act as intermediaries 
                    between our principals and the people of Oman. As discussed 
                    earlier we provide different services for different principals, 
                    in the essential utilities sector. This business model is 
                    working very well since the last 30 years and we are very 
                    proud of the fact that our principals have given us their 
                    trust by giving us the opportunity to provide these services 
                    on their behalf. <br>
                    <br>
                    </strong><strong>What kind of revenues and profits does OIFC 
                    make?</strong><strong style="font-weight: 400"><br>
                    Due to the increase in competition over the years, the commission 
                    rates have really been squeezed. Even then we are striving 
                    to provide more payment modes and better services to our valued 
                    customers. There is a cost factor involved for providing better 
                    services and enhanced facilities to our customers. However, 
                    with the present, moderate commission basis, we face some 
                    limitations and therefore in order to broaden our revenue 
                    stream, we are looking at other income opportunities. Being 
                    a joint stock company, our results are published every quarter. 
                    The capital of our company is RO10mn and during the financial 
                    year 2008-2009 our company made a profit of RO2.032mn. <br>
