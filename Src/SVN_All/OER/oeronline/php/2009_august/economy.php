		        <td valign="top" width="100%"> <p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
                    </font> <b><font face="Verdana" size="4">Carbon boom</font><font face="Verdana" size="1"><img border="0" src="images/img38.jpg" align="right" hspace="8" vspace="6" width="100" height="131"></font><font face="Verdana" size="4"><br>
                    </font><font face="Verdana" size="1">The cap-and-trade scheme, 
                    or carbon exchange, employs market incentives to encourage 
                environmentally-friendly industry and trades carbon just as any 
                other commodity</font><font face="Verdana" size="4"><br>
                    </font></b><font face="Verdana" size="1"> <b> <br>
                    More hot air.</b> That was the result of the G8’s latest round 
                    of stalemate discussions on climate change. Despite the nearing 
                    2012 expiry date of the Kyoto Protocol, America, China and 
                    company failed to make any real 
                    progress on climate change at their meeting last month (June) 
                    in L’Aquila, Italy. But as developing and developed countries 
                    continued to squabble over greenhouse gas emission allowances, 
                    Oman was readying itself to capitalise on the Kyoto agreement’s 
                    cap-and-trade scheme at a moment of growth for both the global 
                    carbon market and the profile of the GCC in the renewable 
                    energy sector.<br>
                    <br>
                    In early July the Sultanate’s Ministry of Environment and 
                    Climate Affairs (MECA) announced that it will set up a National 
                    Designated Authority (NDA) - a Kyoto-mandated body that will 
                    allow Oman to certify clean energy projects to earn tradable 
                    carbon credits.<br>
                    <br>
                    As a ‘non-Annex I’ signatory party to the Kyoto Protocol, 
                    Oman is not bound to meet specific greenhouse gas emissions 
                    targets. However it can sell carbon credits to full Kyoto 
                    members that emit beyond their ‘cap,’ or maximum allowable tonnes of carbon. In other words, it can utilise the Kyoto 
                    Protocol to source outside investment for clean energy projects.<br>
                    <br>
                    <b>Carbon exchange</b><br>
                    The cap-and-<img border="0" src="images/img39.jpg" align="left" hspace="8" vspace="5" width="125" height="228">trade scheme, or carbon exchange, employs market 
                    incentives to encourage environmentally-friendly industry 
                    and trades carbon just as any other commodity. Companies whose 
                    emissions fall under their cap are financially rewarded by 
                    selling excess carbon credits; over-polluting firms are penalised 
                    by being forced to buy credits to meet regulations.<br>
                    <br>
                    Excessive polluters can purchase emissions offset credits 
                    in several forms. The most straightforward are unused emissions 
                    permits from fellow Protocol participants on one of several 
                    emissions exchanges – the largest of which is the European 
                    Union Emission Trading System (EU ETS). Alternatively, under 
                    the joint implementation scheme a firm can earn offset credits 
                    by investing in an emissions-reducing enterprise run by another 
                    Kyoto member. The Clean Development Mechanism (CDM) works 
                    similarly, but is limited to projects in the developing world, 
                    allowing companies to earn certified emissions reduction credits 
                    (CERs) via overseas investment. In each instance one credit 
                    is equivalent to one tonne of carbon.<br>
                    <br>
                    As a so-called developing country, Oman will be operating 
                    under the CDM. All it now needs to enter the market is the 
                    NDA, which is the only body that can lend a Kyoto stamp of 
                    approval to projects, thereby qualifying them to earn carbon 
                    credits.<br>
                    <br>
                    The Authority for Electricity Regulation Oman (AERO) was the 
                    first in the Sultanate to flag the potential of the carbon 
                    trade in developing a sustainable energy infrastructure. In 
                    its May report - the first comprehensive study of renewable 
                    energy in the country - AERO recommended CDM projects and 
                    carbon trading as a means to make renewable energy technologies, 
                    which are often overshadowed by high start-up costs, more 
                    economical. Assuming a price of $25.5/tonne of CO2, AERO estimated 
                    tha carbon trading offered a potential savings of anywhere 
                    from three to 18 percent of capital and operational costs 
                    for solar and wind grids.<br>
                    <br>
                    When the report was released, spot carbon prices on the European 
                    Union<img border="0" src="images/img40.jpg" align="right" hspace="8" vspace="7" width="150" height="212"> carbon exchange were heading up and eventually hit a 
                    high of about $45 in July of 2008. Since then the credit crunch 
                    has pushed the price of carbon down in line with other commodities. 
                    As of mid-July, spot prices were hovering around the $20 mark.<br>
                    <br>
                    But overall in 2008 carbon trading on the EU ETS- which includes 
                    all types of carbon credits –appeared to move against the 
                    tide of the recession. According to a May 2009 report by the 
                    World Bank the market doubled in terms of value and grew by 
                    61 per cent in terms of volume.<br>
                    <br>
                    The global downturn did manage to significantly hamper CDM 
                    development, said the report, as project financing became 
                    ever more elusive. However with the Omani government’s willingness 
                    to spend, solid financial reserves and a demonstrated commitment 
                    to clean energy development, the Sultanate is able to sidestep 
                    financing difficulties, making it an attractive source of 
                    carbon credits for industrialised countries in need.<br>
                    <br>
                    As the market has grown by fits and starts since trading began 
                    in 2005, future stability is highly dependent on positive 
                    indications from international leaders on the importance of 
                    emissions reduction and the role of carbon trading in the 
                    prevention of climate change. In this regard, global consensus 
                    has been weak and with no clear signals on the way forward, 
                    the carbon market has yet to reach its full potential. Growth 
                    has also been constrained by the absence of huge contributors 
                    to global emissions, namely the United States.<br>
                    <br>
                    However, this could be about to change, with the US – under 
                    the most environmentally-friendly leader in years – mulling 
                    over a new climate change law that would mark the entrance 
                    of the world’s highest emitter of CO2 into the carbon market. 
                    While the country is not party to the Kyoto Protocol, the 
                    American Clean Energy and Security Act offers a provision 
                    for a cap-and-trade carbon scheme, which would help to send 
                    a strong, positive signal to the future of the carbon market.<br>
                    <br>
                    Indeed, should the bill pass the American Senate, it would 
                    be the only recent clear signal coming from the developed 
                    world on climate change. The latest meeting of G8 leaders 
                    in Italy came and went with no real advances on greenhouse 
                    gas regulations, except that climate increases should be kept 
                    under two degrees Celsius and developed countries should reduce 
                    their emissions by 80 per cent by 2050. A successor to the 
                    Kyoto Protocol is expected to be laid out at the end of the 
                    year in Copenhagen in order to provide a framework to meet 
                    these goals, though much work still needs to be done.<br>
                    <br>
                    <b>Taking onus </b><br>
                    Meanwhile more and more of the burden of clean energy development 
                    is being taken up by developing nations. There is a perception 
                    amongst industrialised countries that, thanks to their more 
                    advanced technology infrastructure, control over sustainable<img border="0" src="images/img41.jpg" align="left" hspace="8" vspace="7" width="175" height="370"> 
                    technology lies in their court. However this belies the developing 
                    world’s burgeoning role in the sector, which is becoming ever-more 
                    apparent under the lingering weight of the recession on the 
                    developed world. For example, China - often characterised 
                    by the West as a backwater of green development - actually 
                    accounted for the vast majority of transactions in the CDM 
                    market, with over 80 per cent market share in 2008.<br>
                    <br>
                    The Middle East, as well as Asia, is rapidly integrating into 
                    sustainable development circles. In July, Abu Dhabi was named 
                    as international headquarters of the International Renewable 
                    Energy Agency (Irena) - a 136-member organisation aimed at 
                    fostering renewable solutions and international cooperation. 
                    The move was hailed by observers as a ‘triumph’ for all developing 
                    nations, not just the Middle East.<br>
                    <br>
                    With Irena coming to town and a GCC-wide push toward sustainable 
                    development already well underway, the region is expected 
                    to gain momentum in the renewable energy sector. The summer 
                    heat and ubiquitous air conditioning pushes GCC electricity 
                    infrastructure to the brink every year, highlighting the need 
                    for increased capacity. More than ever, renewable energy resources 
                    like solar and wind power seem a viable way forward. Add to 
                    this Irena and the region’s rising profile in the sustainable 
                    energy world and the Gulf is bound to attract more research 
                    and investment in the field. Some predictions for GCC investments 
                    in solar, wind and other renewable energies run as high as 
                    $200bn over the next 20 years.<br>
                    <br>
                    In Oman, MECA’s intention to set up an NDA and its upcoming 
                    presence on the carbon market is just the latest chapter in 
                    the country’s quest toward sustainable development. In May, 
                    the Sultanate’s announcement of tender for its first large-scale 
                    solar power plant was warmly received by international investors 
                    as well as environmentalists.<br>
                    <br>
                    Given its stability and growing reputation for renewable energy 
                    development, Oman’s future proposals in line with CDM criteria 
                    will likely be welcomed by Kyoto parties and others with the 
                    same enthusiasm for investment. The Sultanate’s moves are, 
                    at any rate, considerably more than just hot air.<br>
