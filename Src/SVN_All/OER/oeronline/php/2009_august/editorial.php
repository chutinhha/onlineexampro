                <font face="Verdana" size="1"> <br>
                The fact that family owned firms dominate business in the GCC 
                region is a given. Though many of them started out as trading 
                houses most of them have now become diversified conglomerates. 
                Their growth has giv<img border="0" src="images/img011.jpg" align="right" hspace="8" vspace="6" width="161" height="206">en rise to its own set of challenges like 
                transferring effective control and knowledge from one generation 
                to the next; attracting outside talent and relinquishing 
                control; separation of management and ownership; shifting from 
                being purely operational in thinking to being more strategic in 
                outlook; diversification into unrelated businesses etc. <br>
                <br>
                The recent travails of the Saad Group and Ahmad Hamad Al Gosaibi 
                &amp; Brothers Co from Saudi Arabia has led to concerns about 
                corporate governance and transparency issues in family owned 
                firms in the region. Both the groups defaulted on debts earlier 
                this year and are racing to restructure their empires. In a 
                survey of the region’s top banks, credit-ratings firm Standard &amp; 
                Poor’s found that total gross exposure to the two family-owned 
                firms stood at $9.6bn, with almost two-thirds of those exposed 
                located in Saudi Arabia and the UAE. S&amp;P has termed the exposure 
                as “significant but manageable.” Even though the hit may be 
                manageable, it is sure to lead to some introspection about the 
                way family businesses are run. <br>
                <br>
                Despite such challenges, one cannot dispute the immense 
                contribution that family businesses have made to economic growth 
                in the region. They have been the flag bearers of 
                entrepreneurship and innovation. Though on an average family 
                businesses in the GCC are just over 60 years old, they have 
                generated enormous wealth for all stakeholders. <br>
                <br>
                This is not surprising as research shows that family-run 
                businesses across the world have substantially outperformed 
                non-family businesses of equal size and in the same industry. 
                They now constitute 40 per cent of the large valuation companies 
                in the US and Western Europe. This is attributable to their 
                ability to stick to a set of core values that are vastly 
                different from non-family businesses.<br>
                <br>
                Realising the enormity of the subject we started a series of 
                articles on the next generation of business leaders in family 
                businesses, in July 2008. Periodically, we have covered young 
                leaders from leading business families. Our cover story for this 
                issue, is a continuation of that effort. Urbane and aggressive, 
                confident and hesitant, prudent and reckless, knowledgeable and 
                naive – the gen next is a bundle of contradictions. Whether you 
                like them or hate them, there is one thing that you just cannot 
                do – ignore them. <br>
