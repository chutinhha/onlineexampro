              <p align="justify" style="margin-left: 10px; margin-right: 10px"><font size="1" face="Verdana, Arial, Helvetica, sans-serif"> 
                <br>
                </font><b><font face="Verdana" size="4">Shaping the Future</font></b><br>
                <b><font size="1" face="Verdana">Traditionally family businesses 
              do not survive beyond the third generation. Oman seems to have 
              bucked the trend with the WJ Towells, Naranjee Hirjees and Khimjis 
              way into their fourth and fifth generations. Can the younger 
              generation carry on this legacy? Visvas Paul D Karra finds out</font></b></p>
              <p align="justify" style="margin-left: 10px; margin-right: 10px"> 
                <font size="1" face="Verdana"> Till 2009, the Al Yousef Group’s 
                (AYG) hospitality division was an also ran business, though the 
                group co-owned stella<img border="0" src="images/img22.jpg" align="right" hspace="8" vspace="8" width="350" height="466">r hotels like Crowne Plaza, Muscat and the 
                Riveria in Bahrain. All this changed with, Ghada Al Yousef, 
                daughter of Dr Mohamed 
                Musa Al Yousef, Chairman of AYG, taking over as the managing 
                director of the hospitality division. As a start, she opened the Ubhar restaurant at Bareeq al Shatti in late 2008. The 
                restaurant, serving Omani fusion cuisine, has taken off to rave 
                reviews. Not content, Ghada is working on taking the hospitality 
                business to a new high. Apart from this she is also working with 
                HSBC as manager, sales &amp; service, personal financial services.<br>
                <br>
                Al Zaman Investments, a relatively young business group has 
                investments in several large corporates and the retail business. 
                When Khalid Zaman joined the company as a Director, he boldly 
                forayed into the real estate business with a few residential 
                projects which met with success. The group has developed 
                properties all across Muscat and is currently planning to 
                develop two new hotels, a 4 star business hotel and a 5 star 
                deluxe hotel. Khalid has chalked up plans of entering the 
                high-end restaurant business with exotic cuisines now.<br>
                <br>
                Four years ago, Hritik A Khimji took over Qurum Training 
                Insitute (QTI), an IT company fighting for survival. Hritik 
                rechristened QTI as Mosaic and turned around the company. Today, 
                although Hritik is heavily involved in the operations of Khimji 
                Ramdas as a Director (specifically overlooking the 
                Infrastructure Group), he proudly relates himself to Mosaic.<br>
                <br>
                <b>Renaissance children</b><br>
                Ghada, Khalid and Hritik represent a younger generation of 
                entrepreneurs from various business families in the Sultanate. 
                This new generation of leaders, would be shaping Oman’s 
                tomorrow. Not content in b<img border="0" src="images/img23.jpg" align="left" hspace="8" vspace="6" width="400" height="285">asking in their family’s wealth and 
                glory, they are gradually moving into key positions in their 
                respective businesses. They are the proverbial “Renaissance 
                children” having been born post 1970. An era when the country 
                had shed its obscure past and began marching forward under the 
                wise leadership of His Majesty Sultan Qaboos bin Said.<br>
                <br>
                It would not be far-fetched to say that this young generation 
                has been groomed by the country itself. His Majesty, laid 
                stress on developing human resources as one of the four 
                imperatives of nation building along with developing natural 
                resources, building infrastructure and the establishment of 
                state institutions. The future of any country lies in its youth 
                and with over 50 per cent of the population in Oman being under 
                the age of 25, their importance can hardly be overstated. <br>
                <br>
                With family businesses being torch bearers of economic activity 
                in the Sultanate, their fortunes play an important role in the 
                nation’s development. Prof John L Ward, clinical professor and 
                co-director at Centre for Family Enterprises, Kellogg School of 
                Management and an authority in family business says that 
                family-run businesses across the world have substantially 
                outperformed non-family businesses of equal size and in the same 
                industry. They now constitute 40 per cent of the large valuation 
                companies in the US and in Western Europe. He attributes their 
                success to ‘behaving differently’ and having a set of core 
                values that are vastly different from non-family businesses. 
                This holds true even for Oman. <br>
                <br>
                “The main focus of our generation is to maintain the same values 
                of our ancestors and at the same time move forward,” says Hritik 
                w<img border="0" src="images/img24.jpg" align="right" hspace="8" vspace="5" width="350" height="247">ho is the 5th generation of the Khimjis. According to him, the 
                Khimji Ramdas business would not have lasted for five 
                generations without sticking to the core values and principles 
                like the trust between the family members, integrity in business 
                dealings, and human values. “Our doors are open to everyone 
                right from the top to the bottom most. As directors, we maintain 
                personal relations with our staff. We visit our native place in Kutch, in Gujarat, India and keep the family ties intact. Also, 
                there are thousands of things required to run the business but 
                trust and care are of paramount importance, and our core values 
                should never be eroded.” <br>
                <br>
                Qais Al Khonji, director of Al Khonji Group and various 
                companies like Oman Fisheries, Oman National Engineering and 
                investment company, and Entrepreneurs organisation (Oman 
                chapter) says, “In the family business, all interested members 
                of the family get together to develop and follow a mission 
                statement that describes why they are committed to the business. 
                In allowing family members to share their goals, needs, 
                priorities, strengths, weaknesses, and an ability to contribute 
                in planning help create a unified vision of the company that 
                will guide future dealings.” <br>
                <br>
                <b>High expectations</b><br>
                Having stepped into 
                <img border="0" src="images/img27.jpg" align="left" hspace="8" vspace="5" width="250" height="410">adulthood recently, the younger generation, 
                like their peers from other well to do families like driving 
                expensive cars, living it up with their friends and enjoying 
                their weekends over a game of golf. But this youthful exuberance 
                is manifest in their approach to business as well, with their 
                energetic attitude bringing in a new sense of purpose in their 
                companies. <br>
                <br>
                However, the weight of expectation is a burden felt by these 
                young Turks in equal measure. Like parents who want their 
                children to do well in life, the patriarchs who have built up 
                the business expect their children to continue the family 
                legacy. “Comparisons inevitably arise and that is when the 
                pressure falls on us,” says Raid Sultan, assistant business development manager, 
                Alargan Towell Investment Co, a part of the WJ Towell Group. 
                Raid Sultan is a fifth generation scion of the W J Towell family. 
                Illustrating his point, Raid draws parallels between a doctor 
                and his son. If the father was a good doctor and the son too 
                becomes a doctor, people invariably compare the two. <br>
                <br>
                Hayyan Ali Malallah Al Lawati, business development manager, Al 
                Habib &amp; Co, says that preserving wealth is more challenging than 
                creating it and that is where the challenge lies for this 
                generation and it actually creates a lot of pressure as they are 
                expected to continue at the same pace. <br>
                <br>
                Enhanced competition is another factor making life tougher for 
                the present generation. Hayyan points that nowadays competition 
                is not just limited to the local market but comes from various 
                quarters like regional and multinational companies. Since it is 
                a family business, the family expects the youngsters to grow the 
                business at a certain pace and diversify both locally and 
                internationally. “We are representatives of our previous 
                generations and so everyone has certain expectations from us,” 
                says Hayyan whose father Ali Malallah Habib Al Lawati, 
                challenged conventional wisdom by starting Al Habib &amp; Co in 
                1978, offering professional services in the real estate sector, 
                dominated by self employed individuals<img border="0" src="images/img25.jpg" align="left" hspace="8" vspace="14" width="250" height="291"> at the time.<br>
                <br>
                “You are expected to not only expand the existing family 
                business but also you are supposed to pave the way for the next 
                generation to take the business forward. But although the 
                expectations are high, the kind of support you get from the 
                family members in the business is a great asset, observes Nikhil 
                Sampat, director, marketing of Naranjee Hirjee, a 104 year-old 
                institution. He readily accepts that he has learnt so much from 
                his seniors that no textbook or multinational company in the 
                world could have given him the same learning. <br>
                <br>
                <b>Foreign exposure</b><br>
                One of the biggest advantages of the younger generation is their 
                exposure to foreign business cultures and a different working 
                environment because most of them have been educated and have 
                lived for many years abroad. Moreover, they have specialised in 
                subjects keeping in view the requirements of their family 
                business. Hritik, the fifth generation of the Khimji family, for 
                example, studied Finance and Entrepreneurship from Babson 
                College, USA, after six years in Switzerland. Similarly, Tariq 
                Al Barwani, studied as a geologist in London before joining 
                National Mining Company, a part of the M B Holding Company 
                founded by his father Mohammed Al Barwani.<br>
                <br>
                Nikhil says that it was already decided that he would eventually 
                join the business and he was brought up with that idea during 
                his schooling and education in England. Since the family 
                business is mostly into distribution and trading, he graduated 
                with an international degree in business studies focusing on 
                marketing. Says Nikhil, “This was something, which was in my 
                head, you know, since a very young age that I would eventually 
                join the family business. I was always more focused on getting 
                educated in marketing because that would be my expertise when I 
                returned to Oman. Now I am heading the FMCG non-food division of 
                the business.”<br>
                <br>
                <b>Tech savvy</b><br>
                The other strength of this generation is their exposure to 
                technology and communication, which was absent 10-15 years ago. 
                “The information technology revolution has brought everything t<img border="0" src="images/img30.jpg" align="right" hspace="8" vspace="5" width="250" height="397">o 
                the click of a button. Earlier things were in a classical world 
                but now you can sit at your desk and do everything online, right 
                from applying for company registration number to checking for 
                new projects. And then you have the mobile which has become so 
                ubiquitous that you can reach anyone, any-where, anytime. This 
                saves time and energy which can then be channelised elsewhere,” 
                points out Khalid. <br>
                <br>
                Concurs Hayyan while saying that networking has become more 
                accessible through the use and the improvement in technology. 
                Since everyone is clued in to the other, although the 
                competition has increased, still most of the young leaders are 
                friends with each other. <br>
                <br>
                Qais says that the strength of his generation lies in their 
                ability to be flexible, informal, and their drive to display 
                their entrepreneurial skills through innovations. The other 
                important strength for the new generation is that there is whole 
                set of company employees who are committed and loyal and it 
                makes it easy to work with them and they share the same values 
                and belief system. Since Omanis are generally family oriented, 
                Qais thinks that he derives his strength from the family spirit; 
                family name; family dream; and a strong sense of mission and 
                vision.<br>
                <br>
                <b>Challenges ahead </b><br>
                The strong values and family traditions can sometimes also act 
                as an Achilles’ heel for the younger lot as they may find that 
                it is at times difficult for the organisation to change even 
                though there is a perceived necessity to adopt modern practices. 
                “The younger generation is in danger of losing track of the 
                values and ethics of the seniors because of the lack of proper 
                interaction between the various generations, says Raid. Such 
                interactions help in thrashing out differences, while retaining 
                the family principles and traditional values, he adds.<br>
                <br>
                Nikhil says that the younger generation is more aggressive and 
                impatient and has a more textbook orientation as compared to the 
                experience, which was garnered through real life situations by 
                the older generation. “The inability to have clarity of thought 
                and objectivity and emotionally charged decision-making leads to 
                mixing up family issues with business problems,” says Qais while 
                adding that the new generation is unable to balance family’s and 
                business’s need for liquidity.<br>
                <br>
                “Spoon-feeding and heavy pamperi<img border="0" src="images/img26.jpg" align="right" hspace="8" vspace="5" width="400" height="286">ng by the parents is a big 
                weakness of this generation,” says Hayyan. Parents have to groom 
                their children from an early age to educate them about wealth, 
                and how it is created, how they can be part of making, improving 
                and preserving the business, rather than showering them with 
                money. “I have seen cases where the kids are given so much 
                money, that there is no pressure for them to learn or work, it 
                spoils the meaning of life. I admire businessmen and parents who pressurise the upcoming leaders to not work directly in the 
                family business, but to get international and local exposure,” 
                adds Hayyan<br>
                <br>
                <b>Professionals versus family </b><br>
                Inducting professionals in a family business is an important 
                factor which raises shareholder value and helps a company to 
                grow. Such non-family managers in crucial roles bring in a 
                variety of perspectives and competencies to a company. The 
                support of people with diverse backgrounds and talent is 
                increasingly important <br>
                in any business. <br>
                <br>
                Tariq says that professionals are an extremely important part of 
                the family business. And even training and mentoring other 
                professionals within the system is important. Having worked in 
                different places and companies professional managers bring in 
                fresh ideas. “We are Omanis – my brot<img border="0" src="images/img29.jpg" align="left" hspace="8" vspace="5" width="347" height="327">her Usama, my father and 
                myself – and we do think similarly, partially because we are 
                family and partially it is our group. But professionals bring in 
                a new perspective,” says Tariq. <br>
                <br>
                Al Zaman group has a pool of highly talented executives who run 
                and manage the various business units of the group and who have 
                been working with Khalid’s father Mohammed for the past 20 years 
                which was before he joined the company. “I look at them as a 
                source of inspiration and they help me out with whatever I need. 
                They in turn look at me as a beacon of new generation. They 
                respect me and whatever fresh ideas I have they always welcome 
                it. But it is not that I can just go to my father and get 
                approval for everything. They are involved in all the decisions 
                I take,” Khalid says.<br>
                <br>
                <b>Leaning on experience</b><br>
                Despite the difference in viewpoints between the old and new 
                generation, most are quick to acknowledge their debt to their 
                fathers. Tariq says that though his generation is more 
                energetic, intelligent and impulsive but they still have a long 
                way to go to catch up with the preceding generation. “On at 
                least two occasions, I found that my father, Mohammed Al Barwani, 
                founder of MB Holding, was right after all. My father is very 
                aggressive but I am more conservative as a geologist being in 
                the mining field. But he always pushed me. We definitely need to 
                respect the elders and draw from their experience,” he says. <br>
                <br>
                “There are differences between the old and the new generation 
                but it is not like old versus new. It is a combination of 
                individual responsibilities coupled with the market 
                environment,” says Ghada. According to her, you have two 
                different generations working out there and that is tough to 
                balance. You have to manage a diverse group of people who have a 
                traditional approach and then you have a modern workforce being 
                pumped into the company every year. <br>
                <br>
                <b>Sibling rivalry</b><br>
                Most family businesses across the world face the challenge of 
                selecting the most competent family members from the next 
                generation to lead the business. In Oman, such problems have not 
                been manifest as most families have put in place a clear cut 
                succession plan. In this way sibling rivalry has been avoided to 
                a large extent and is almost negligible compared to other parts 
                of the world. <br>
                <br>
                “I am sure every family business runs into conflict at one point 
                of time or another but the important thing is how you manage it. 
                You need to differentiate between the family members based on 
                their individual talent, like my brothers and myself have been 
                given specific roles within the group b<img border="0" src="images/img28.jpg" align="right" hspace="8" vspace="6" width="350" height="292">ased on our skills. 
                Further, as Arabs, we have strong family values,” says Ghada. 
                Instilling the right vales in the next generation helps in 
                succession planning, says Hritik. “It is our duty also that we 
                do our planning both for the company and for our next generation 
                as well. If there is a value system then succession planning 
                takes a backseat,” he adds.<br>
                <br>
                The younger generation is confident that their education, 
                experience and attitude will help them in furthering their 
                family fortunes. However, detractors may argue that born with a 
                silver spoon in their mouths, these lads still have a long way 
                to go. Well, the jury is still out … keep watching this space. </font><br>
