                        advertising terms, that is a good scenario for the 
                        Company that continues to spend on brand-building and 
                        advertising as it is doing so with clutter levels in the 
                        media being much lower. And believe you me; it takes a 
                        lot of conviction and courage to spend on advertising in 
                        recessionary times. But those who continue to do so, 
                        come out much stronger during and after the recession.</font></td>
