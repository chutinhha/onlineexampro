						<b> <font face="Verdana" size="1" color="#FFFCE8">
                        HOW MUCH OF A BRAND’S SUCCESS CAN BE ATTRIBUTED TO 
                        ADVERTISING?</font></b></td>
  </tr>
  <tr>
                      <td height="82" valign="top" width="351"> <p align="justify" style="margin-left: 5; margin-right: 5">
						<font face="Verdana" size="1">Advertising plays an 
                        important but limited role in the success of a brand. 
                        Advertising helps introduce the product and has a great 
                        role in creating a positioning of the product and the 
                        brand in the minds of the customers. Advertising also 
                        helps keep the brand alive in the minds of the 
                        customers. But a brand’s success depends on innumerable 
                        other factors which are both tangible and intangible. 
                        Product, service, retail atmosphere, pricing, quality 
                        etc play an important role in determining the success of 
                        the brand at a tangible level. At an intangible level 
                        there could be many other factors viz environmental, 
                        financial, social, geographical etc. which may determine 
                        the success of the brands.</font></td>
    <td width="5" height="82" bgcolor="#BCBEC0"></td>
