						<b><font face="Verdana" size="1" color="#FFFCE8">SHOULD 
                        COMPANIES INCREASE AD SPENDS IN TOUGH TIMES OR CUT 
                        ADVERTISING BUDGETS?</font></b></td>
  </tr>
  <tr>
                      <td height="96" valign="top" width="351"> <p align="justify" style="margin-left: 5; margin-right: 5">
						<font face="Verdana" size="1">Companies should act smart 
                        and look at the SOV (share of voice) they generate with 
                        the amount of advertising money they spend. Even though 
                        during tough times the corporate behaviour (read 
                        marketing budgets) changes for every company, one should 
                        not loose sight of the long term goal – market share or 
                        leadership and advertise to maintain or achieve it as 
                        the case may be. The decision to spend less or more 
                        depends on various factors like marketshare ambitions, 
                        competitive scenario etc. Among the many variables the 
                        only thing that is a constant for great companies is the 
                        drive to stay on top. Tough times or good times, one 
                        thing that remains clear is that the companies that come 
                        out tops never loose sight of one important aspect – 
                        that of Brand Building by continuously investing in the 
                        brand.</font></td>
    <td width="5" height="96" bgcolor="#BCBEC0"></td>
                      <td height="96" valign="top" width="402"> 

<p align="justify"><font face="Verdana" size="1">I think this is a trick 
question. It’s like asking me if the patient in critical health requires more 
attention and better medical care or should we leave him alone and hope he’ll 
start doing better. Similarly, marketing wisdom would suggest that companies 
should spend more in tough times to woo that elusive customer. There is hardly 
any choice in the matter! If the business is to get back in health, attracting 
customers is inevitable. And advertising is therefore sacrosanct. Having said 
that, let’s also remember that during tough times, all the elements of the 
marketing mix have to work in tandem rather than over-dependance on advertising 
alone. Advertising might attract the customer to the shopfront but it’ll take 
more than advertising alone to clock the sale.</font></p>

