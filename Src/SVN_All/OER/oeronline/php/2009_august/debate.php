						<p style="margin-left: 5; margin-right: 5" align="justify">
						<font face="Verdana" size="1">To quote Bill Bernbach, 
                        ‘Bad advertising can’t harm a good product, but good 
                        advertising will only make a bad product fail faster.’ 
                        And that is my answer to your question. Let’s always 
                        remember that the product or service is at the heart of 
                        any business. Once you have the 3Ps (Product, Price and 
                        Physical distribution) managed well, only then can the 
                        4th P (Promotion) deliver on its stated marketing 
                        objectives. Similarly, if the other 3Ps are well 
                        orchestrated but the Promotion element is missing, it’s 
                        probably best described by the old advertising cliché, 
                        “Doing business without advertising is like winking at a 
                        girl in the dark. You know what you are doing but nobody 
                        else does”</font></td>
