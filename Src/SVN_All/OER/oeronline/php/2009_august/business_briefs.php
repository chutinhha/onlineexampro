                <b>Abu Timam Grant Thornton welcomes new global IFRS standard </b></font><font size="1" face="Verdana, Arial, Helvetica, sans-serif"><br>
                    The new International Financial Reporting Standard for small 
                and medium-sized entities (IFRS f<img border="0" src="images/img8.jpg" align="right" hspace="8" vspace="5" width="110" height="128">or SMEs) could transform the 
                way privately held businesses (PHBs) around the world prepare 
                their accounts said Grant Thornton, as it welcomed the 
                publication of the global standard by the International 
                Accounting Standards Board (IASB). The global accounting 
                organisation believes the new standard offers a unique 
                opportunity to create a tandardised accounting framework for 
                privately held businesses throughout the world. IFRS for SMEs 
                provides a substantially simplified set of internationally 
                recognised accounting principles for PHBs. Based on the full 
                IFRSs, which were developed primarily for listed companies, the 
                new standard will particularly benefit businesses that operate 
                internationally. Nasser Al Mugheiry, managing partner, ATGT 
                says, “The potential of this new standard is that we move to a 
                situation where lenders and investors are able to assess company 
                performance from financial statements that use directly 
                comparable, authoritative, internationally recognised 
                principles, regardless of the country of origin of the company 
                itself. This could improve access to capital and help 
                decision-making on cross-border deals, quite apart from reducing 
                the administrative burden on PHBs.” <br>
