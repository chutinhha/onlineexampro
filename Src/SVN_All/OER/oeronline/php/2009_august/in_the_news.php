                    </font><b><font face="Verdana" size="4">Shanfari Group, 
                Lightworks Resources tie-up for designer furniture</font><br>
                    <br>
                    </b> <font face="Verdana" size="1">The Shanfari Group of 
                Companies has entered into a business deal with Lightworks 
                Resources Inc 
                to distribute designer furniture from the Philippines in Oman 
                paving the way for Om<img border="0" src="images/img13.jpg" align="right" hspace="8" vspace="8" width="350" height="263">anis to experience a unique kind of 
                furniture never seen before in this country. This partnership is 
                being hailed as yet another step in the strong cultural and 
                economic relations shared by Oman and Philippines and also 
                highlights the excellent economic ties between the private 
                sectors of the two countries.<br>
                <br>
                “We are excited at the prospect of opening a distribution point 
                and showroom in this part of the world. I’m sure it will act as 
                a window into the kind of exclusive and artistic furniture that 
                we are making, exhibiting and selling all over the world”, said 
                Ravi Singh and his wife Rashmi, founders of Lightworks, who were 
                in Oman in July to finalise the deal with the Shanfari group.<br>
                <br>
                While acknowledging and appreciating the warmth and hospitality 
                of their host Sheikh Adil Shanfari, vice chairman of Shanfari 
                Group, Ravi and Rashmi were grateful to Omar Pangarungan, head 
                of cultural and economic diplomacy, at the Philippine embassy in 
                Muscat. “Pangarungan’s office links the businessmen of both the 
                countries and without his active help, support and involvement, 
                it would not have been possible to have such meaningful dialogue 
                with the right kind of people and to forge such strong links,” 
                said Singh.<br>
                <br>
                It may be noted that the Philippine embassy’s cultural and 
                economic diplomacy office is tasked with implementing the 
                Filipino government’s development diplomacy, which refers to how 
                the country conducts its foreign relations in order to enhance 
                its position in the vital areas of culture, trade, investment, 
                finance, tourism technology and aid. Arranging the visits of 
                business delegations to Oman is one of the activities of 
                Pangarungan’s office.<br>
                <br>
                Says Pangarungan, “Omani businessmen are just beginning to 
                discover the true economic potential of the Philippines and the 
                same holds true for my countrymen as well.” In June, 
                Pangarungan’s office had facilitated the visit of Imelda 
                Madarang, one of Asia’s leading business women and 
                Vice-President of RFM Corporation, which exports halal food from 
                the Philippines. RFM Corporation’s halal-certified meat 
                processing plant in Laguna is the only one in the Philippines 
                and Madarang was in Oman to explore the possibilities of 
                increasing the volumes and diversification of exports to this 
                country. <br>
                <br>
                Pangarungan also disclosed that a large Filipino trade 
                delegation comprising of government officials and private 
                investors is also expected to visit Oman before the end of this 
                year.<br>
                <br>
                Philippines is regarded as the Milan of Asia as far as designer 
                furniture is concerned. Furniture here is not just meant to 
                fulfill functional purpose. Lightworks came into existence in 
                1987. Starting off with furniture made from exotic indigenous 
                timber known as Narra, Ravi moved on to making wall art and 
                furniture. The public, the elite and the critics alike very well 
                received an exhibition of Singh’s artistic furniture and 
                accessories.</font><b><br>
