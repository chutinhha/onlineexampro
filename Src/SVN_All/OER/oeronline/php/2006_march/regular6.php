                <p align="justify"><font face="Verdana" size="1"><b>Nike files 
                lawsuit against Adidas</b><br>
                Nike has charged that its German rival adidas has stolen its 
                cushioning technology for use in its latest sports shoe models. 
                The American firm has filed a patent infringement lawsuit in 
                Texas against adidasSalomon claiming treble damages for theft of 
                Nike’s Shox cushioning system. The writ alleges that adidas 
                footwear, including the Kevin Garnett signature shoe and the 
                adidas 1 model were developed with patented Nike technology. The 
                courtroom bust-up is the latest confrontation between Nike and 
                adidas, respectively first and second in the global shoe 
                business. The footwear foes have been engaged in an extended 
                technology and fashion duel that is now focused on the US 
                market, where adidas has hoped to chip away at Nike’s dominant 
                position. While Nike has long led the field since the 
                development in the 1980s of air capsule cushioning technology to 
                replace layers of polyurethane foam, adidas has recently been 
                catching up with its own high-tech soles and last year agreed to 
                merge with Reebok, that gives the German firm extra clout with 
                American retailers. The two companies have vied with each other 
                in launching ever more outrageous and higher-priced footwear, 
                the latest addition to the adidas stable being the adidas 1, a 
                challenge to Nike’s Air Jordan. Priced at $250, the adidas-1 
                contains a microprocessor and claims to be the world’s first 
                computerised shoe. </font></p>
