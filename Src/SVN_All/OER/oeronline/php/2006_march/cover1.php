            <p align="left"><br>
			<font face="Verdana" style="font-size: 16pt"><b>‘Reforms aimed at correcting market 
			anomalies’</b></font></p>
            <p align="justify"><font face="Verdana" size="1"><b>
			<img border="0" src="covers1b.jpg" width="100" height="136" align="left" hspace="5">Mehre 
			Alam catches up with Dr. Hussain M. Al Mahdi, Chairman of Gulf 
			Global Consultancy and a leading economist and thinker, holding 
			crucial positions in Bahrain’s economic bodies. Dr. Hussain talks at 
			length on the issue of labour market reforms – its genesis and 
			implications</b><br>
			<br>
			<b>What was the basic idea behind labour market reforms in Bahrain?
			</b><br>
			If you take into account the recent economic developments in 
			Bahrain, there are two sides to the labour market reforms: 
			opportunities and challenges. One really has to mull over certain 
			demographic changes that Bahrain has undergone in the past 14 years.
			<br>
			<br>
			From the 1991 Census to the latest one in 2004, one sees a marked 
			shift in the share of the population from Bahrainis to 
			non-Bahrainis. The increase in the population of non-Bahrainis has 
			been higher than the increase in the population of Bahrainis. 
			Between these two censuses, Bahrainis grew at a rate of 37 per cent 
			against 47 per cent for non-Bahrainis. So there is a 10 per cent 
			gap. Of the total population, the share of non-Bahrainis went up 
			from 36.4 per cent in 1991 to 38 per cent recently. <br>
			<b><br>
			What is the implication of this situation?</b><br>
			As per the 2004 Census, Bahrainis registered an annual population 
			growth rate of 2.6 per cent, against 2.9 per cent for non-Bahrainis. 
			And we have this issue of unemployment in Bahrain. Between June 2000 
			and June 2005, total employment increased at almost 60 per cent. But 
			the private sector was the dominant player and it grew at 67 per 
			cent. Its share in the total employment rose from 83.6 per cent in 
			2000 to around 88 per cent in June 2005. <br>
			<br>
			The employment of Bahrainis in the private sector labour market 
			dropped from 32.2 per cent in 2002 to around 25 per cent in June 
			2005, registering a negative growth. And the share of non-Bahrainis 
			increased from 67.8 per cent to 75 per cent in the same period. So 
			among any four positions in the market, three are held by 
			non-Bahrainis! <br>
			<br>
			Importantly, non-Bahrainis dominate the private sector. That is why 
			many initiatives are being mulled over and called for. Labour market 
			reform is one such initiative to remedy this situation. From now on 
			to 2013, we have to create 10,000 jobs a year. If we don’t, the 
			unemployment figure, which stands at 20,000 now, will touch the 
			70,000-mark in 10 years. This will imply a lot of strain on the 
			labour market, economy, society, as well as on the political system.
			<br>
			<b><br>
			How are the reforms going to remedy this situation? </b><br>
			The Bahraini economy needs to create 100,000 jobs in the next 10 
			years. I think the Bahraini economy can do it. It is possible! <br>
			<br>
			But how are we going to create this many jobs? Well, a majority of 
			them should be absorbed by the private sector, which employs 88 per 
			cent of the total workforce. We are now seeing a shift of emphasis 
			towards private sector jobs in Bahrain. And it is an international 
			phenomenon.<br>
			<b><br>
			How much load can the public sector take? </b><br>
			Over the next decade, nearly 100,000 Bahrainis will enter the labour 
			market. The public sector cannot absorb them. However, the types of 
			positions/job opportunities provided by the private sector still do 
			not meet the expectations and requirements of the Bahrainis, 
			especially those scouting for jobs at the middle or higher levels, 
			thanks to the cost and competition factors. Expatriates, on the 
			other hand, accept the same positions at lower salaries. <br>
			<b><br>
			What about the aspects of education and training? Are the locals 
			prepared for such a transition? </b><br>
			Our education system does not prepare the Bahrainis with the skill 
			sets required by the labour market. On the other hand, expatriates - 
			at middle to higher positions - come with strong backgrounds and 
			experience.<br>
			<b><br>
			What are the other advantages for expatriates? </b><br>
			Expatriates are favoured over Bahrainis in the private sector 
			because of the work ethic. It’s easy for some locals to work for one 
			individual one day and then simply not show up the next day, as they 
			have got something else to do. Besides, many start late and leave 
			the office early. Now, as a private sector employer, one has 
			bottom-lines. Private companies have to bear the productivity factor 
			in mind when employing individuals.<br>
			<b><br>
			Why don’t Bahrainis cherish joining the private sector? </b><br>
			We have people with higher degrees but unfortunately their 
			qualifications and skill sets, like communication skills, do not 
			match the requirements of the private sector. For example, 70 per 
			cent of the Bahrainis taking the TOEFL test come a cropper. <br>
			<br>
			Incentives provided by the private sector do not give the Bahrainis 
			sufficient motivation. Businessmen in Bahrain hardly invest in 
			training. And Bahraini employees are pressing for training. Labour 
			market reforms will surely address this issue.<br>
			<br>
			Bahrainis also feel that even if they work hard, there are not 
			enough incentives for them in the private sector. <br>
			For example, there is hardly any salary hike. On the other hand, the 
			cost of living has been going up, steadily, over the years. So the 
			Bahrainis are saying: Try to match our salaries with our 
			expectations and you’ll see how hard we can work.<br>
			<br>
			The average salary for Bahrainis has been dropping. This is 
			alarming. We need labour market reforms. <br>
			<b><br>
			What should be done to regain the confidence in the labour market?
			</b><br>
			There are two things to regain the confidence in the labour market:
			<br>
			1) Spur strong growth in the private sector. This will create more 
			job opportunities with middle and higher salary structures. And in 
			order to register a strong growth in the private sector in Bahrain, 
			we’ll have to have more liberal rules and regulations, which will 
			give more incentives for the private sector to grow. There is the 
			need to make the private sector the main vehicle for growth, <br>
			2) Make Bahraini workers the preferred choice for the private 
			sector. More Bahrainis need to take up private sector jobs but for 
			that to happen, the productivity of the Bahrainis has to go up too 
			so that they become the preferred choice for the private sector. <br>
			We have to see that the skill sets and qualifications of Bahrainis 
			match the needs and requirements of the labour market. We have to 
			remove all bottlenecks that make Bahrainis in any way less 
			attractive or suitable for the private sector. <br>
			<br>
			We have to close the gap of incentives between the private sector 
			and the public sector. This implies that the salaries and benefits 
			of the two sectors should be made more compatible.</font></p>
