<p align="justify"><font face="Verdana" size="1"><b>Nokia’s new bluetooth 
headsets</b><br>
Nokia has launched three new bluetooth headsets. The tiny and stylish BH-800 
weighing only 9 grams is available in coffee black and silver white colurs. The 
headset incorporates an optional, removable ear loop, for use in either ear, in 
addition to a strap that allows the wearer to place and remove the headset with 
one hand. It comes with a talk time upto six hours and upto 160 hours of stand 
by time. The powerful BH-900 is an elegant headset with a comfortable and 
ergonomic earhook that can easily be switched from one ear to the other. It has 
good audio quality in noisy environments as it supports both noise reduction and 
echo cancellation. The headset weighs 18 grams and supports Push to talk over 
bluetooth wireless technology. The sleek BH-200 is an easy to use wireless 
headset with comfortable ear hook and convenient function keys. Weighing 14 
grams, it completes the Nokia bluetooth headset trio with very user friendly 
functionality and a feeling of simplicity. It has talk time of up to five hours 
30 minutes and up to 150 hours of stand by time.</font></p>

