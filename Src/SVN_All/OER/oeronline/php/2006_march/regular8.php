<p align="justify"><font face="Verdana" size="1"><b>Panasonic Plasma &amp; LCD TV 
with recording</b><br>
Panasonic has introduced its latest range of Viera plasma and LCD televisions in 
the Sultanate of Oman. The hi-definition plasma &amp; LCD models come with a SD slot 
wherein the SD card can be inserted and the programmes can be directly recorded. 
The Viera series boasts of a new G8 plasma panel and 3D color management system 
with 4 trillion colours basic processing for 8.6 billion on screen colors. The 
new range includes, two high definition plasma models – TH-50PV500 and 
TH-42PV500; two high definition LCD TVs – TX-32LX500 and TX-26LX500; two 
standard resolution wide plasma TVs – TH-42PA50 and TH-37PA50 and one standard 
resolution LCD TV – TX-23LX50.<br>
With the Viera flat screen televisions, Panasonic is going beyond the limits of 
conventional television and is attracting the customers with high standard 
picture quality, tone, networking and user-friendliness. These models are 
available at all Panasonic retail stores, select dealers and hypermarkets across 
Oman. The Panasonic retail outlets are offering additional benefits also upon 
purchase of Viera plasmas and LCD’s.</font></p>
