<p align="justify" dir="ltr"><font face="Verdana" size="1"><b>Race among 
international oil majors hots up to bag bids for oil exploration blocks in Oman</b><br>
<br>
Four Indian companies, in consortium with Australia’s Oilex NL, may be close to 
winning the bids for one of the two exploration blocks they had pitched in for, 
in the Sultanate. The Consortium, according to reports, had made bids worth $50 
million for the two blocks.<br>
<br>
It may be noted that the Sultanate’s Oil and Gas ministry had invited bids for 
five of its onshore exploration blocks – 54, 55 and 56 which sit in the Eastern 
Flank area, and 57 and 58, which are located in the Western Margin region of the 
Sultanate. These blocks were relinquished by the 60 per cent government-owned 
Petroleum Development Oman (PDO). According to reports, the deadline for 
companies to submit bids was January 1, 2006. These sites, covering an area of 
more than 23,000 sq. km, are believed to have billions of barrels of crude 
reserves. The blocks — 54, 55, 56, 57 and 58 — were relinquished by PDO in 
several concessions. <br>
<br>
Oil majors from the US, China, Malaysia, besides India, are reportedly eyeing 
these blocks. Consultants representing companies from these countries have been 
exploring the scope of investing in Oman’s oil and gas sector. As for the Indian 
companies, state-run GAIL (India) Ltd., Hindustan Petroleum Corp. Ltd., Bharat 
Petroleum Corp. Ltd., private sector company Videocon Industries Ltd., and Oilex 
have bid together for the blocks 56 and 58, which are located off south Oman 
salt basin. <br>
<br>
PDO had sought some clarifications on the work programme offered by the Indian 
companies. GAIL, Videocon and Oilex will reportedly hold a 25 per cent interest 
each in the block after the acquisition, while Bharat Petroleum and Hindustan 
Petroleum will have 12.5 per cent each. The Indian companies reportedly expect 
to be formally awarded the block by next month. <br>
<br>
Meanwhile, talking to Oman Economic Review, Mohammed Al Barwani, the Chairman 
and CEO of MB Petroleum, said, his company had also bid for two of the five 
blocks. “Prospects for one of the two oil blocks is good but we are not sure 
about the other,” Barwani said, while not revealing the block numbers his 
company has bid for. He further revealed that a final decision on the oil blocks 
could come in two to three months. When asked if PDO had any role, even a minor 
one, to play in the decision-making process, he categorically denied it. “PDO 
doesn’t have anything to do in finalising the bidder,” he said. <br>
<br>
MB Petroleum Services is a multinational corporation providing drilling and 
integrated well services worldwide. A subsidiary of MB Holding Company, MB 
Petroleum Services (MBPS) is one of the most successful and the largest quality 
oilfield services provider company in the Middle East. <br>
<br>
According to the bidding rules, in case of tie between two or more companies, 
the government would offer the block to the bidder that has offered a higher 
signature bonus. A signature bonus is an up front payment representing a 
fraction of the investment that is required to extract gas and oil. The 
signature bonus, therefore, makes the bidding process more competitive. <br>
<br>
The government has already renewed its Block 6 concession and operating pacts 
with foreign oil majors Shell, Partex and Total for another 40 years. The 
concessionaries can carry out petroleum operations for oil in Block 6 for the 
next 40 years, effective from January 2005 to December 2044. It may be noted 
that the Omani government has been encouraging foreign oil companies to invest 
in exploration and production. The government, in fact, already has a programme 
of acreage releases, and foreign companies have been investing in Oman. The oil 
and gas sector, which is one of the main pillars of the Omani economy, enjoys 
the presence of a number of international oil companies with well-established 
operations.<br>
<br>
Recently, PDO announced that it had discovered four new oil fields. The 
declaration comes as timely and welcome news for the Sultanate, which has 
reserves that are both limited and difficult to extract. PDO has been in the 
forefront of initiating the plans for enhanced oil recovery (EOR) techniques as 
well as exploring for new field.<br>
<br>
In fact, the Government of Oman has recently signed a pair of landmark pacts 
with a consortium of investors designed to ramp up production from the 
potentially prolific Mukhaizna field. The agreements, that involve investments 
of several billions of dollars over the operational life of the project, would 
contribute in a major way to the Sultanate’s ongoing efforts towards reversing 
the trend in falling oil output. It may be noted that the daily average 
production of oil of Oman for the year 2005 was estimated to be 753,000 barrels. 
The Sultanate earned in excess of $7 billion from oil in 2004. <i>- Mehre Alam</i></font></p>

