<p align="justify"><font face="Verdana" size="1"><b>Matt Harrison sheds light on 
how a direct mail campain can work wonders for marketers</b><br>
<br>
Everybody says they hate all the unsolicited mail that pours into their inbox 
every day. Yet, secretly, everyone opens at least some of the stuff they 
receive. Indeed, research suggests that done right, direct mail can deliver 
meaningful returns. However, I think that most marketers at one stage or another 
have been involved in direct mail campaigns that simply bombed – thousands and 
thousands of mail shots were sent out and you got a zero response rate. This 
month’s column is intended to look at how a direct mail campaign can work – a 
bluffer’s guide, if you will.<br>
<br>
Oman-based companies are always looking to grow their business by finding the 
right customers - at the right time - for their products and services. With 
tight marketing budgets, they have to ensure the greatest return on investment 
and reduce their expenses – enter stage left, direct mail. To be frank, direct 
mail is a valuable activity which deserves a place in the promotional toolbox of 
every Oman-based marketing professional. It’s flexible, responsive and 
measurable. It can be used to keep existing customers in the loop or gain new 
ones. Unlike other promotional tools, direct mail can be tested and refined and 
possesses a unique ability to communicate effectively with both the masses and a 
single individual, all at the same time.<br>
<b><br>
Be focused</b><br>
Sounds too good to be true? What about the pitfalls? There are a number of 
issues to consider on both a strategic and tactical level. Generally speaking, 
who are you targeting? For example, will it be sent to new or existing clients, 
or a mixture of both? If the latter, will the mailing content change to 
acknowledge that some recipients may know your organization well, while others 
have limited information on who you are and what you do? <br>
Treating everyone the same ignores much of the flexibility offered by direct 
mail and risks alienating loyal existing clients. The folk who put food on your 
table.<br>
<b><br>
Build it and they will come</b><br>
What can make or break a direct mail campaign is data – is it up-to-date or was 
it last reviewed Q3 2004? Indeed, poor data quality and accuracy can invite a 
host of problems. Incorrect addressing looks unprofessional and wastes money. 
Mailings to the wrong contact name or decision maker usually end up in the bin. 
Even worse, mailings to recipients who have recently passed away can cause 
distress and generate extremely negative publicity. Don’t forget to make it easy 
for those who wish to unsubscribe from your mailing list. An email or a simple 
telephone call could be the possible avenues.<br>
<br>
A database can be a simple list, or a complex computerised system running across 
your in-house Intranet. As such, there’s no single, step-by-step approach to 
building a database. There are a variety of packages available on the market 
that will help you build a database. The most common of these is Microsoft 
Access and for many applications this system is perfectly acceptable. Larger 
organisations with multi-tasking and relational requirements may need more 
sophisticated solutions, such as Oracle multi-user systems. Take time to assess 
the needs of your business as a whole and then invite specialists to advise on 
how best to move forward. <br>
<br>
Consider carefully what your recipients will receive. Create a mailer that’s 
creative and has an eye-catching design, memorable and punchy copy, along with 
an easy to find response mechanism. Unfortunately, some Omani companies see 
direct mail as a means of using up old or out of date brochures and corporate 
literature, which isn’t only totally unsuitable for the task but speaks volumes 
about how they perceive their current and potential clients. <br>
<b><br>
How often &amp; when?</b><br>
A lot of marketers worry about how often they should use direct mail. There’s no 
real answer to this question. Frequency is generally dictated by the needs of 
your market and of course your budget. However, the timing of the campaign can 
be everything. For example, will it capitalise on other awareness-raising tools 
such as advertising, public relations, a conference or trade show? In simple 
terms, the Muscat Inter-Continental might mail its client list on a seasonal 
basis and Caledonian College in advance of each term or intake.<br>
<br>
On a tactical level, you might want to consider which day of the week the 
mailing should arrive with the target audience. For example, many e-mail 
marketing campaigns are broadcast midweek to get the best results, but home or 
leisure oriented mailings are better promoted on a Wednesday, just before the 
weekend. <br>
<b><br>
That all important letter</b><br>
Your direct mail must contain a well-written and succinct covering letter. The 
language you use is crucial. Avoid lengthy and uninspiring talk and never use 
negatives such as unfortunately, cannot, won't or regrettably. In brief, address 
the reader directly and use plenty of uplifting positive language.<br>
<br>
There’s no magic formula for laying out and writing the perfect direct mail 
leaflet, and there’s no evidence to suggest short copy is any better than long 
copy. The important consideration when looking at copy length is the existing 
knowledge of your potential customers and the type of product/service you’re 
pushing. The copy can be longer if it’s interesting and well laid out and your 
potential customer has little knowledge about your product or service. As a rule 
of thumb, use bullet points, major selling points and use sub-headings to break 
up long copy. <br>
<br>
Try to find ways to involve prospective customers. This could be a free gift 
that’s relevant to the sales message or it could be a testimonial by a trusted 
third party<br>
<b><br>
Your sales pitch should be structured like this:</b><br>
• Attention - the headline or envelope message.<br>
• Interest - tell them something that’s relevant and beneficial.<br>
• Desire - the prospect needs to desire your service/product.<br>
• Conviction – reassure the prospect – quote a satisfied client. <br>
• Action - tell your prospect what to do next.<br>
<b><br>
Typeface</b><br>
Research suggests that the serif typefaces - Times, Courier and Palatino – are 
the best to use. Research has tested the reader's ability to comprehend 
information and assess concepts from work they’d viewed. The use of serif type 
was in some cases up to five times as effective as sans faces.<br>
<br>
In direct mail, unusual shapes and materials bring a higher return. Avoid the 
predictable. If your mailing looks plain and boring, then you could forgive the 
recipient for thinking that your offer will be boring too. Textured papers, 
unusually shaped envelopes and a number of mailings sent out in a sequence all 
improve uptake. This type of approach won’t be the cheapest option to roll out, 
but you should get a bigger bang for your Rial. <br>
<b><br>
It’s done &amp; dusted</b><br>
Once you’ve carried out your direct mail campaign you’ve got to evaluate the 
results. In this regard, some of these techniques will help: <br>
• Compare the results from one mailing list or selection with another. <br>
• Test offers against each other to see what works and what doesn’t. <br>
• Try mailing the same offer at different times of the year. <br>
• Try changing the headline, layout or the visual to see what impact this has on 
response rates. <br>
• Look at the way people respond to you. For example, do they contact you by 
phone, fax, e-mail, or through your website?<br>
• Over time you will see how your target audience wants to respond to you.<br>
<b><br>
Start a fire</b><br>
Remember, all these ideas can work really well. However, if your mailing list 
isn’t up to the mark; your offer’s unattractive; and the timing is off, your 
direct mail material will end up lighting a camp fire on a Thursday night 
somewhere in the interior.</font></p>

