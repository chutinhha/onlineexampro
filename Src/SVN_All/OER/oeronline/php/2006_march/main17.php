<b>Web exclusive/Interview with Prof. Andrew Kakabadse</b></font><font face="Verdana" size="1"><br>
<br>
<b>Making of a great leadership</b><br>
In today’s dynamic global market, the leaders are made in boardrooms but great 
leadership is made in workshops conducted by management gurus. In February, 
College of Banking &amp; Financial Studies in association with Central Bank of Oman 
organized “Visioning Workshop – Attributes of World Class Leaders” in Muscat for 
the benefit of leaders in private and public sector organisations. Oman Economic 
Review was the official media partner of the event.<br>
<br>
The workshop was conducted by Andrew P Kakabadse, Professor of International 
Management Development and Community Director of Leadership and Organisation 
Development at Cranfield School of Management, Cranfield University, UK. He has 
consulted and lectured in the UK, Europe, US, SE Asia, China, Japan, Russia, 
Georgia, the Gulf States and Australia. <br>
<br>
He has published 26 books, over 148 articles and 16 monographs, including the 
best selling books ‘Essence of Leadership’, ‘Politics of Management’, ‘Working 
in Organisations’ and ‘The Wealth Creators’. His two latest books are entitled, 
‘Geopolitics of Governance’ and ‘Smart Sourcing: International Best Practice’.
<br>
<br>
Prof. Kakabadse’s current areas of interest focus on improving the performance 
of top executives and top executive teams, excellence in consultancy practice, 
corporate governance and conflict resolution and international relations. His 
database for the study of strategic skills of top teams covers 17 nations 
including Japan, China, Hong Kong and the USA and thousands of private and 
public sector organizations. He is expected to embark on a major world study of 
boardroom effectiveness and governance practice. <br>
<br>
Besides his future course of action in Oman, Prof. Andrew talked at length with 
Akshay Bhatnagar on the challenges ahead of leadership and what goes into the 
making of a great leader. Excerpts of the talk:<br>
<b><br>
What was the objective of the workshop?</b><br>
The top managers in leadership role hold accurate and powerful insights 
concerning the strategic, operational and by implication, the leadership 
challenges facing their organisation. The concern is whether such insights can 
be ‘raised to the surface’ and meaningfully discussed. The workshop assisted 
each of the participants to appreciate the nature of the challenges they face in 
their ‘real life’ work related context and to support the development of the 
necessary leadership capabilities to enhance their ability to address known 
concerns, now and into the future.<br>
<b><br>
Besides the workshop, what brings you to Oman for the second time (he conducted 
a similar workshop in Oman last year also)?</b><br>
I want to introduce more research related activity in the region especially in 
Oman. There are no Gulf wide research benchmarks. There are cultural and 
economic disparities within the region but that’s what makes the task more 
challenging to us. The international business environment is also equally if not 
more diverse in nature. <br>
<b><br>
…but not many organisations and government bodies in the region are willing to 
impart with the data they have?</b><br>
This is true. We have to be sensitive in doing our research. We need to develop 
better relationships with in the region. This kind of a hindrance is not unique 
to the region alone. There are other markets, say China, which is far more 
stringent and cagey in sharing the data. Same is the case with Russia. It’s a 
matter of developing an understanding of the market and influencing the right 
people to carry out research activities as per your objective. <br>
<b><br>
You talked of more activities in Oman. Can you elaborate more on this?</b><br>
There is a plan to set up a leadership centre in Oman. I’ll be one of the 
founding members of the centre. It will focus on executive education, coaching 
of leadership (a relatively new concept in the region) and development of 
potential leaders in different sectors. The research, which can drive the 
business, will obviously be at the core of our operations. We will be looking at 
arranging international affiliations for the centre. I’ll take care of the 
research and development related activities and set up the benchmarks to follow.
<br>
<b><br>
Have you ever been involved in setting up a similar kind of an institution 
before?</b><br>
No. I have never done this before. I have been busy with other kinds of numerous 
international projects. <br>
<b><br>
What have been your impressions about leadership in public and private sector in 
the region specifically Oman?</b><br>
The first and the most striking impression is that Oman is very community 
oriented. This feature is missing greatly in the Anglo-American world. There is 
a strong sense of community belonging here. But there is a question mark on the 
application of the market disciplines. I still think the leaders are too 
concerned in making tough decisions. Do they make it quickly enough? May be 
there is a need to introduce a greater speed of responsibility among the 
leaders. <br>
<b><br>
Your opinion is valid for other gulf countries also or you find it unique to 
Oman only?</b><br>
I don’t have too much experience of other Gulf states. I have been to UAE which 
I believe is quite different from other Gulf countries. <br>
<b><br>
Do you think leadership across markets shows similar traits?</b><br>
One of the first issues that worried me when I was doing the research on 
boardroom effectiveness and world-class chairmen was that we couldn’t find any 
pattern. The study was done across hundreds of companies in different markets. 
The learning for us is that the leaders are distinctive – depending up on the 
condition, situation, crisis and local culture. When you look at them you do 
find certain similarities but that has got nothing to do with their character or 
gender. It’s related to their age. The older, more experienced and more well 
traveled were often the best. So one of the myths that older people are not good 
enough to take or continue in leadership positions is broken.<br>
<b><br>
…but globally the trend is more towards younger persons holding the leadership 
positions?</b><br>
I agree this is the trend worldwide. But in most of the markets, the leaders who 
are comparatively older are doing very well. <br>
<b><br>
There is another belief that older leaders fail to keep pace with new technology 
and trends. How far is that true?</b><br>
We found even this to be a complete myth in our research. The inability to keep 
pace with new trends has got nothing to do with age. It has got more to do with 
getting ‘stuck’. If you are in a job for 20-years, you may find that you have no 
future. Take the other case. Let’s say you have been in a job for 15-years and 
you are rigid and not learning anything new. You might be just 35-year old and 
acting in a manner in which we expect a 50-year old to behave! So it has got not 
much to do with age but got more to do with not having a chance or willingness 
to learn something new. I know of a person who is 72 year old and he can walk, 
run and work faster than me. <br>
<b><br>
Is there any other myth which was broken through your research?</b><br>
Yes. It is related to the gender bias. I have been to so many countries and met 
a wide variety of people in leadership positions. I have found that women are no 
different from men. They are as good or as bad and can get as aggressive. But 
the myth is true to a certain extent. The myth works in the struggle to ‘getting 
there (in the leadership position)’ due to factors including prejudice and 
social conflicts but once females reach ‘the leadership position,’ it is a very 
different story! They behave and perform similar to the way their male 
counterparts behave and perform. <br>
<b><br>
Like you said one should not stick to a particular job for his whole life, is it 
right for leaders to hop from one industry to another or they should be glued to 
one particular industry segment only?</b><br>
It’s better to change jobs but it is not a good idea to move from one sector to 
another. We found that those who were jumping to other sectors caused more 
problems. Take the example of persons moving from automotive sector to banking 
sector, we found more failures in our database. <br>
<b><br>
How important is creativity and decision making ability to become a successful 
leader on a sustainable basis?</b><br>
Again it varies because one has to see where is the value propositions for the 
company. There are many companies which prefer their leaders not to be creative. 
I can think of a large number of companies which are making good profits and 
work in an efficient manner with less stress on creativity. But there are many 
other companies which survive on the basis of their creativity. Microsoft relies 
on innovation. I’ll give you another example from the airline sector. In 
Australia, an unknown airline took over small aircrafts which nobody liked to 
fly in and converted them into business class and started flying on just one 
route - Sydney to Melboune. Today it is a highly profitable airline. That’s 
leadership creativity at its best. Honestly speaking, I haven’t met too many 
leaders who are very innovative. Many of them are good in one or two areas but 
they are not innovative in too many areas.
