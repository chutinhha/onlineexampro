                        <img border="0" src="bsb1.jpg" width="150" height="66" align="left">Oman 
						Air posts RO1.1 million profits</b><br>
						Oman Air has posted a net profit of RO1.1 million for 
						2005 against RO828,000 in 2004, as per the financial 
						results (unaudited) declared recently. The total 
						revenues reached RO75.89 million in 2005, up from 
						RO65.64 million in the previous year. Ziad Al Haremi, 
						Acting Chief Executive, Oman Air informed that 2005 was 
						unique for Oman Air, since it commenced international 
						operation on 20 March, 1993, with the company achieving 
						numerous accomplishments in all spheres. The most 
						significant is the growth of passengers by 15.4 per cent 
						from 984,000 in 2004 to 1,135,450 in 2005. As a result, 
						seat factor was increased to 75 per cent from 69 per 
						cent. The time performance and technical reliability of 
						planes recorded 99.6 per cent, which is higher than the 
						global standard of 97 per cent. With the sixth jet, 
						which joined the fleet at the beginning of 2005, all the 
						flights were rescheduled, to increase the capacity of 
						sectors witnessing increasing traffic.</font></p>
						<hr width="80%" size="1">
						<p align="justify"><font face="Verdana" size="1"><b>
						Omantel profits dip by 5.8 per cent </b><br>
						Oman Telecommunications Company (Omantel) has reported a 
						net profit of RO68.6 million for 2005 as against RO72.8 
						million for 2004. The net profits are down by 5.8 per 
						cent. Omantel had earlier said that the total revenue 
						for the first nine-month period of 2005 rose by 6.9 per 
						cent to RO193.9 million from RO181.44 million for the 
						same period of 2004. The operating cost also soared by 
						17.6 per cent to RO136.3 million by September-end from 
						RO115.9 million for the same period of 2004.</font></p>
						<hr width="80%" size="1">
						<p align="justify"><font face="Verdana" size="1"><b>
						BankMuscat in restructuring mode </b><br>
						BankMuscat is gearing up for future challenges. The 
						board of directors of BankMuscat has approved the bank’s 
						restructuring plan based on recommendations made by the 
						global strategy consulting firm, Booz Allen Hamilton. 
						This is expected to result in significant changes to the 
						bank’s current organization structure, particularly at 
						senior management levels. <br>
						<br>
						The restructuring process, which was initiated last 
						year, will help the bank focus greater energies on key 
						and emerging customer segments in Oman and across the 
						region. <br>
						<br>
						Recently, BankMuscat also organized its first 
						“International Managers’ Workshop”. It was attended by 
						the senior managers of BankMuscat International 
						(Bahrain), Centurion Bank of Punjab (India), BankMuscat 
						Dubai Representative office and a team from the 
						soon-to-be-opened BankMuscat operations in the Kingdom 
						of Saudi Arabia besides BankMuscat offices in Oman. <br>
						<br>
						Central Bank of Oman approved BankMuscat’s financial 
						statements for the year ended 31 December, 2005. CBO 
						recommended an amendment in cash dividend to 35 per cent 
						(in addition to stock dividend of 10 per cent). 
						BankMuscat had achieved a net profit of RO 45.4 million 
						for the year ended 31 December, 2005, as against a net 
						profit of RO 34.1 million for the year ended 31 December 
						2004, registering an increase of 33.1 per cent. </font>
						</p>
						<hr width="80%" size="1">
						<p align="justify"><font face="Verdana" size="1"><b>OAB 
						declares 33.3 per cent dividend</b><br>
						Oman Arab Bank has reported a 32 per cent increase in 
						profits in the year ending 2005. The bank made a profit 
						of RO13.69 million (highest ever in its history) in 2005 
						agaisnt RO 10.35 in 2004. OAB has also declared a record 
						cash dividend of 33.3 per cent. In compliance with 
						Central Bank of Oman’s regulation for minimum capital of 
						RO 50 million, the bank’s board has decided to increase 
						the share capital of the bank to RO38 million by a 
						rights issue of RO 6 million and transfer RO 2 million 
						from retained earnings in early 2006. The bank plans to 
						increase the share capital as per CBO norms within the 
						timeframe set by CBO. </font></p>
						<hr width="80%" size="1">
						<p align="justify"><font face="Verdana" size="1"><b>
						Banking in Oman on roll</b><br>
						As per the Central Bank of Oman, the total assets value 
						of the operating commercial banks in the Sultanate had 
						increased to RO 5.561 billion by the end of 2005, up 
						from RO 4.888 billion in the previous year. These 
						figures translate into a 13.8 per cent growth. The total 
						net profit value of the operating commercial banks in 
						the Sultanate increased to RO 125 million by the end of 
						2005, up from RO 79 million in the previous year. These 
						figures translate into a 57 per cent growth.</font></p>
						<hr width="80%" size="1">
						<p align="justify"><font face="Verdana" size="1"><b>
						<img border="0" src="bsb2.jpg" width="150" height="85" align="left">Indo-Oman fertilizer plant opened </b><br>
						The mega fertilizer plant established by the Indo-Oman 
						Joint Venture Company (OMIFCO) was inaugurated recently 
						at Sur. The project will produce 16.5 lakh tons of urea 
						and 2.48 lakh tons of surplus merchant ammonia annually. 
						The entire production of urea will be procured by the 
						Government of India under a 15-year long-term urea 
						off-take agreement and the ammonia production will be 
						procured by IFFCO (another India company) under a 
						10-year Ammonia off-take agreement. The company, in a 
						little over six months of operation, has earned a profit 
						of around US$ 50 million. <br>
						<br>
						A statement issued by the Indian embassy said that there 
						was substantial scope of Indian joint ventures in Oman 
						in petro-based downstream projects which will be 
						discussed during the GCC-India Business Conference 
						scheduled to be held in Muscat. The opportunities for 
						investment by Omani companies in the proposed mega 
						chemical industrial estates as well as in the areas of 
						organic and inorganic chemicals, dyes and speciality 
						chemicals were also highlighted. The Oman Oil Company 
						and the Sohar Port Development Authority made detailed 
						presentations for the visiting Indian delegation on the 
						projects being undertaking by them in Sohar Industrial 
						Port as well as other investment opportunities, 
						particularly in downstream projects that were possible 
						in this newly emerging industrial hub.</font></p>
						<hr width="80%" size="1">
						<p align="justify"><font face="Verdana" size="1"><b>OIB 
						profit up by 64.8 per cent</b><br>
						Oman International Bank (OIB) has posted RO 22.03 
						million of net profits, growing by 64.8 per cent over 
						the last year. The bank's board has proposed 
						distribution of 23 percent of the cash earnings, in 
						addition to 20 percent of free stocks. The bank's net 
						interest income rose from RO 27.49 million to RO 32.51 
						million, due to robust growth in the net loans.</font></p>
