						<p align="justify"><font face="Verdana" size="1"><b>
						Danish products off Omani shelves</b><br>
						Many of the retail chains in Oman have withdrawn Danish 
						products from their shelves. As per reports, Lulu 
						Hypermarkets removed all Danish products — chilled and 
						dairy products, frozen and dry foods and chocolates, 
						biscuits and cookies — from its shelves. <br>
						<br>
						Carrefour, another leading retailer, also withdrew all 
						Danish products from its single outlet in Oman at Seeb. 
						Family Shopping in Ruwi and several other smaller 
						supermarkets had also pulled out all Danish goods. Most 
						of the nations with significant Muslim population have 
						boycotted Danish products to register their protest 
						against the appearance of offensive cartoons that 
						appeared in newspapers in Denmark.</font></p>
