						<p align="justify"><font face="Verdana" size="1"><b>New 
						trading system at MSM</b><br>
						The Muscat Securities Market (MSM) has adopted a new 
						electronic trading system to upgrade market efficiency 
						and boost investors’ confidence. It has also facilitated 
						trading through the Internet. According to Ahmed bin 
						Saleh al Marhoon, MSM’s Director-General, the new system 
						is flexible in providing the market with future 
						requisites such as new financial investment instruments. 
						According to him, the French trading system was chosen 
						to replace the current trading system due to its success 
						in 17 advanced financial markets in Europe, America, 
						Asia and the Middle East.<br>
						<br>
						The Government of Oman is also studying the option of 
						privatizing MSM to boost both liquidity and depth of the 
						bourse. A Paris-based solutions provider for securities 
						industry has suggested converting MSM into a private 
						company. The boards of MSM and CMA are reviewing the 
						proposal. </font></p>
