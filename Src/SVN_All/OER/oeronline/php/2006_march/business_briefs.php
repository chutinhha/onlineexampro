						<p align="justify"><font face="Verdana" size="1"><b>
						<img border="0" src="bsb3.jpg" width="150" height="88" align="left">Take-off for new Gulf Air 3-year plan</b><br>
						Gulf Air has announced a new three-year strategic plan 
						that makes provision for significant investment in key 
						areas of the business to deliver sustained growth over 
						the long term. “Smart Airline, Successful Business” 
						includes provision for re-capitalization of the airline, 
						re-equipment of the fleet, product upgrades and 
						refurbishment of present aircraft, and investment in a 
						range of areas of the business. The resolutions in 
						support of the new planwere passed at the first Board 
						meeting under Gulf Air’s new ownership structure, which 
						sees the Kingdom of Bahrain and the Sultanate of Oman 
						now each owning 50 per cent of the airline, following 
						the withdrawal of Abu Dhabi as a shareholder. James 
						Hogan, President and Chief Executive of Gulf Air, said: 
						“I am delighted with the Board’s strong support for our 
						new business plan. Smart Airline, Successful Business 
						will take us forward strongly, consolidating Gulf Air’s 
						position at the forefront of the global aviation 
						industry. With the support of shareholders that are 100 
						per cent committed to the success of our business, this 
						airline will reinforce its position as a world class 
						service brand with the strongest regional network in the 
						Middle East.” </font></p>
