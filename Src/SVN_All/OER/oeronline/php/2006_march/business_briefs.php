						<p align="justify"><font face="Verdana" size="1"><b>Oman 
						Awards for Excellence in May</b><br>
						The eighth edition of the Oman Awards for Excellence, 
						the prestigious business accolade for excellence in the 
						private sector of Oman, is going to be organized on 13 
						May this year. Apart from the regular categories 
						including entrepreneurship, tourism, exporting, public 
						service initiative, people, and the overall best of 2005 
						“Flame of Excellence Award,” a new category 
						“Environmental Award” has been created this year. 
						Talking about the new award, Philip Stanton of Ernst &amp; 
						Young from the organizing committee said, “The 
						environmental award is focused on environmental 
						achievements which compliment an organisation’s 
						profitable growth, and other contributions to the 
						economy of Oman.” The entry for the awards are going to 
						be open between 1-31 March and the applicants can refer 
						to the website www.omanawards.com for detailed 
						information.</font></p>
