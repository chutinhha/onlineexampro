						<p align="justify"><font face="Verdana" size="1"><b>
						<img border="0" src="bsb5.jpg" width="150" height="83" align="left">Shangri-La Oman opens with a bang</b><br>
						Shangri-La’s Barr Al Jissah Resort &amp; Spa is going to 
						host the World Summit on Innovation &amp; Entrepreneurship 
						during 1-3 April this year. The global event of such 
						large scale is happening in Muscat for the first time. 
						Earlier on 27 February, Shangri-La’s Barr Al Jissah 
						Resort &amp; Spa was inaugurated by HH Sayyid Haitham bin 
						Tariq Al Said, Minister of Heritage and Culture. The 
						resort is expected to give a major fillip to Oman’s 
						tourism landscape. It comprises of three hotels. Al Husn 
						has 180 rooms and offers Marrakesh Restaurant (Morrocan) 
						and private beach bar. Al Bandar, the vibrant heart of 
						the resort with the touche of opulence, has 198 rooms. 
						Al Waha, with 302 rooms, is an enchanting hotel ideal 
						for families with a variety of accommodation and food 
						and beverage experiences. Each of the hotels has got a 
						different design. <br>
						<br>
						The launch of Shangri-La’s Barr Al Jissah Resort &amp; Spa 
						has given a tremendous boost to Oman’s prospects as a 
						MICE destination. HE Eng. Ahmed Al Dheeb, Undersecretary 
						of Oman’s Ministry of Commerce and Industry said, “The 
						launch of Shangri-La has provided the Sultanate greater 
						opportunity to aggressively market its tourism potential 
						to the global industry looking for a large scale 
						world-class infrastructure. <br>
						<br>
						Before the commencement of Shangri-La, it would have 
						been difficult for Oman to host an event of the scale of 
						World Summit.” </font></p>
