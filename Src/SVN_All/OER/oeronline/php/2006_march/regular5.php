    <font face="Verdana" size="1"><b>Unemployment: a ticking time bomb?</b></font><p align="justify" style="margin: 10" dir="ltr">
    <font face="Verdana" size="1">Addressing the Dubai Press Club last month, 
    Professor Klaus Schwab, founder and executive chairman of the World Economic 
    Forum, said the widespread Arab unemployment was a ticking time bomb, likely 
    to explode if not defused in time. The Professor said, “The region must 
    generate more than 100 million job opportunities in the next ten years. The 
    goal can only be met by creating an environment conducive to open minds and 
    by providing the sufficient liquidity, thus encouraging the private sector 
    to set up many projects.” Assessing further, he added, “The job creation in 
    the region depends in the first place on competitiveness. For this reason, 
    the Arab leaders must help their countries become more competitive than 
    others.”<br>
    <br>
    The challenge in front of the Arab world is indeed daunting! Statistics 
    reveal the real picture. The Middle East and Northern Africa (MENA) region 
    had average economic growth of 3.7 per cent in the period 1998-2002, which 
    rose to 5.4 per cent in 2005. And in 2006, it is expected to be 5.6 per 
    cent. But the region needs to grow by at least 6-7 per cent every year to 
    create enough jobs for the new work force. <br>
    <br>
    With oil prices hovering around $60 per barrel, the windfall revenues need 
    to be channeled into development activities on a sustainable basis. Prof. 
    Schwab did recognize the efforts made by the Gulf region on this front as he 
    said, “The Gulf countries are heading in the right direction. As a proof, 
    they are adequately investing their oil surpluses in education and 
    universities. Besides, they seek to diversify their income sources away from 
    oil.” <br>
    <br>
    Saudi Arabia, as per the annual budget for this year, has allocated funds 
    for constructing 2,673 new schools, three technical colleges and 15 
    vocational training centers. Dubai is investing billions of dollars in 
    expanding its airplane fleet to make it the most desirable destination. 
    Qatar is also pumping in its surpluses in building the LNG industry. <br>
    <br>
    However, despite increased investments in creation of new job opportunities 
    and stress on local labour, there is a greater need for further policy 
    reforms to meet the employment needs in the future. Recently, 16 Arab 
    countries in a 19-point declaration to improve the business environment in 
    the region set out common principles and good practices for encouraging 
    investment by pursuing reform policies. The declaration acknowledged the 
    need for transparency and predictability of national policies, laws, 
    regulations, administrative practices and statistics affecting foreign and 
    domestic investment. <br>
    <br>
    A senior member of the MENA-OECD (Organization of Economic Co-operation and 
    Development) Investment programme recently remarked, “To achieve higher 
    levels of investments, policymakers in MENA countries are challenged to 
    rethink their priorities in accordance with investors’ needs. Effective 
    integration into the world economy requires us to pursue a comprehensive 
    reform: economic, political, social, educational, legal and judicial. For 
    such reforms to be successful and sustainable, they should be 
    self-initiated.” <br>
    <br>
    A regional office for the World Economic Forum in Middle East, on the lines 
    of similar kind of Forum offices in the USA and China, is planned in the 
    next two-three years. Davos Forum is one of the most important international 
    economic forums, which is attended by world leaders and groups over 1,000 
    international companies annually. The opening of the regional office is 
    expected to enhance the investment opportunities in the region considerably. 
    China and India, touted as the next economic superpowers, make it a point to 
    hard sell themselves at the Davos forums regularly to attract global 
    investors. Holding Middle East Davos Forum in May in Egypt’s Sharm el Sheikh 
    Resort is also a step in the right direction. <br>
    <br>
    The Middle East region has 7.5 per cent of the world’s population. But it 
    contributes just 2.5 per cent of the global economic production. The time 
    has come to correct this equation. There is no doubt that the region has lot 
    of potential for growth. It’s a matter of integrating itself in the global 
    economy to correct the imbalance. To achieve this, it needs to get into the 
    right kind of mind-set, professes Mr. Schwab.</font></td>
