<p align="justify" dir="ltr"><b><font face="Verdana" style="font-size: 16pt">
<br>
</font></b><b><font face="Verdana" size="5">So Near, Yet So Far</font></b><font face="Verdana" size="1"><br>
<b><br>
GCC single currency is a gigantic economic dream. OER takes stock of the 
progress made on this front</b></font></p>
<p align="center" dir="ltr">
<img border="1" src="cover2.jpg"></p>
<p align="justify" dir="ltr"><font face="Verdana" style="font-size: 14pt"><b>A</b></font><font face="Verdana" size="1">lmost 
five years after the historic Muscat Summit agreement on ushering in a single 
currency in the Gulf Cooperation Council (GCC), the gigantic economic move is 
sputtering. The optimism evident on December 21, 2001 in Muscat, among the royal 
guests gathered for the 22nd summit is still vivid to some. The assemblage’s 
intentions were noble. They wanted to emulate the European Union and the single 
currency that was the shop talk in the corridors of power across the globe. The 
GCC members, rolling in petro-dollars, were jubilant as they hugged each other 
in the typical Arabian style after signing the path-breaking agreement for 
economic union among the member countries. <br>
<br>
The purpose is plain and simple: to activate economic integration between the 
GCC states and to enhance the progress being made towards establishing a common 
market and monetary and economic union. No agreement over economic integration 
will be complete without any reference to the monetary union: a common currency 
for the entire region. Naturally, the Muscat Summit of 2001 contained clauses 
relating to the establishment of a monetary union of GCC members and the launch 
of a single currency, which specified a time table for the monetary union,.<br>
Besides, “it also directed the Financial and Economic Cooperation Committee to 
agree on the measures of economic performance needed for the monetary union to 
succeed by the end of 2005 with a view to launching a single currency in January 
2010,” points out Khalid Abdulla Al-Bassam, the former Deputy Governor of 
Bahrain Monetary Authority, offering his country’s perspective. “We believe that 
linking the GCC currencies to the US dollar paves the way for a single currency 
that would promote intra-GCC trade and boost the GCC economies. It would also 
create favourable conditions for dealings with international economic blocs,” he 
adds.<br>
<b><br>
Still a Dream</b><br>
Yet, when the GCC rulers assembled once again in Dubai, in December 2005, the 
dream of creating the right climate for ushering in a single currency by the 
target date of 2010 could not become a reality. All the basic elements of 
economic integration such as the GCC Customs Union, the GCC Common Market, the 
GCC Monetary Union and the harmonisation of policies, are progressing… though 
there is much to be done, according to GCC Secretariat Minister Plenipotentiary 
and director-economic integration department Dr Abdel Aziz Abu Hamad Aluwaisheg.<br>
<br>
The six mainly oil exporting states aim to unify their dollar-pegged currencies 
within five years, and have set Eurozone-style limits on debt, interest rates, 
budget deficits and inflation to bring their economies closer into line. To 
achieve this, it is important for all the GCC states to first have similar 
interest rates, inflation rates, level of reserves, public debts and budget 
deficits before the introduction of a common currency.<br>
<br>
Similarly, a real single currency means a single central bank and a unified 
monetary and fiscal policy. Member countries are not allowed to incur deficit in 
their budgets (the European Union allows a deficit not exceeding 3 percentage 
points of the GDP). Deficit in the budget of a member country means borrowing 
from the central bank, which in turn means that this country is overspending, at 
the expense of its partners. In other words, a unified currency leads to giving 
up part of the country's sovereignty to a central authority. Such a step can be 
taken only by mature nation states.<br>
<br>
When Dr Aluwaisheg laments slow progress, he is not alone. “The fact that some 
GCC states have pursued unilateral trade policies and signed bilateral free 
trade agreements over the past few years has certainly complicated the economic 
integration progress,” elaborates Emilie Rutledge of Dubai-based Gulf Research 
Centre (See Interview: “Agreement on convergence criteria, a big hurdle”) Points 
out another Aden-based observer: “It took more than a decade since the 
establishment of the Council in 1981 for the Gulf countries to come around their 
differences and stand on common grounds…. The only fear is that this collation 
would be influenced by political disturbances. For example, the dispute that 
took place between Bahrain and Saudi Arabic recently when the former joined the 
US without consulting with the other GCC countries, an issue that led to 
internal friction in the union for sometime”.<br>
<br>
Al Bassam, however, does not attach too much weight to the political angle. 
“Examination of the structure and operation of the economies reveals substantial 
similarities. Likewise, the economic and financial policies followed by the 
respective governments and monetary authorities are quite consistent. There 
appears to be no major conflicts about policy objectives. Internally, domestic 
fiscal and monetary practices are generally conducive to investment. Externally, 
the authorities follow policies to promote an open trade system,” says the 
former BMA top honcho.<br>
<b><br>
Fresh Perspective</b><br>
Sociology Professor Dr Mohamad Abdullah Al Mutawal of UAE University has an 
interesting viewpoint. According to him, the Arab leadership made a great 
achievement by establishing the GCC. “Now it is the turn of the second 
generation of leaders, who came to power in four GCC states, to continue pushing 
ahead with the development march.” It was no secret that the Council was mainly 
established to focus on security issues as a result of political factors that 
dominated the region at that time (the Iran-Iraq war). Of late, there is a 
perceptible shift towards issues of economic importance. “The experiment of 
European countries showed clearly the importance of focusing on economy first, 
then politics,” Dr Mutawal says and adds that “this is why it is so important to 
focus on setting up a common market and creating mechanisms of economic 
integration.”<br>
<b><br>
EU vs GCC</b><br>
Prima facie, comparing the experience of the European monetary integration and 
that of the GCC countries is not correct. For a simple reason, the EU believed 
economic integration without monetary and political integration is not possible. 
To safeguard monetary integration, it created the EU Parliament and Central Bank 
10 years before the launch of the Euro. But in the case of the GCC, no such 
clear vision is seen. They have yet to agree on the structure and power of a 
single monetary authority to oversee the union, a tricky issue.<br>
<br>
The GCC countries have made the movement of national goods, labour and capital 
across their borders completely free as one of the many steps to integrate 
economic and finance systems. They also liberalized land ownership for each 
other’s nationals, adopted a common tariff and coordinated their customs 
administration and procedures. As a process of financial integration, they have 
interlinked their ATM machines and also stock exchanges so as to allow cross 
listing and trade in stocks of companies registered in member countries. In 
practice, however, one may find that some of these issues are not yet completely 
integrated .<br>
<b><br>
Super Economies</b><br>
The fundamental apprehension - at the recently concluded GCC Summit was that the 
super economies of the GCC – Saudi Arabia and the UAE - might dominate the 
union. These concerns are slowing down the political will from economic 
integration. The 26th GCC Leaders Summit failed to yield a timetable for 
monetary union, although the states did endorse five macro-economic and 
budgetary criteria, which might lead in that direction. <br>
<br>
The Council agreed to cap budget deficits at 3 per cent and public debt at 60 
per cent of the GDP. At the same time, inflation will have to be kept by each 
member state to no more than 2 per cent above the GCC average, while interest 
rates can only be a maximum 2 per cent above the average of the lowest three 
member states. In addition, the countries agreed on a requirement that foreign 
reserves cover 4-6 months of imports by 2010. The Summit failed to decide where 
the future central bank would be located and what role it would play in 
regulating the GCC economies.<br>
<b><br>
New Currency, New Era</b><br>
No decision has been taken yet on the name of the new currency and its exchange 
rate arrangement. However, the adoption of common currency, say experts, is 
expected to bring a number of economic benefits. It will eliminate the currency 
conversion costs involved in intra-regional transactions and remove the 
disturbances in relative prices arising from nominal exchange rate fluctuations. 
The lowering of transaction costs and removal of exchange rate uncertainty will 
contribute to raising intra-regional trade, to which GCC countries attach 
considerable importance.<br>
<br>
They also expect that a common currency will act as a catalyst for stronger 
integration and the deepening of GCC financial markets. It will foster the 
integration of money markets across the region and lead to a uniform short-term 
interest rate structure. It will also contribute to the integration and 
development of the region’s bond and equity markets and thereby facilitate 
savings and investments in the region.* The International Monetary Fund’s 
assurance to assist the GCC secretariat in realizing the dream is a big 
positive. A close cooperation between GCC and EU is another plus factor in the 
march towards the new goal.<br>
<br>
2010 is not too far. Time will roll out faster than one envisages. At the most, 
the momentum may slow down for a while. But the day when an Omani walks into a 
Dubai hypermarket and buys his wares with the same currency that he or she uses 
for purchasing goods and services in Muscat, is not too far off. Similarly, 
exchanging Saudi Rial into Qatari Rial or Bahraini Dinar at any of the money 
exchanges will become a thing of history. Single market, single currency is a 
dream project. The Europeans have done it. And the benefits are there for all to 
see. The Gulf nations are on the cusp of duplicating the same success story.<br>
<br>
- Ramesh Kumar with additional reportage from Uma Devi Jadhav in Muscat and 
Sowmya Sri in Dubai</font></p>
