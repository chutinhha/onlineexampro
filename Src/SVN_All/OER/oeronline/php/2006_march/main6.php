		<b>The Benefits</b><br>
        • The elimination of currency transaction costs will result in savings 
        in both time and money. For small and medium-sized companies operating 
        across the Gulf but lacking the necessary expertise in cash management 
        services, it eliminates the cost of converting money from one national 
        currency to another. <br>
        • The elimination of exchange rate risk would encourage increased 
        intra-Gulf trade and investment. <br>
        • Greater transparency in pricing, improved business competitiveness and 
        simplification of procedures and operational issues would generate 
        benefits for the Gulf’s consumers.<br>
        • Fiscal discipline by member countries would contribute towards a lower 
        inflation and interest rate environment within the GCC. Within the euro 
        area, monetary union has encouraged a trend towards declining budget 
        deficits and more prudent fiscal policies.<br>
        • At the micro level, the Gulf currency would have a long-term impact on 
        major regional banks. The impact would be felt on cash and treasury 
        businesses. A single currency encourages more efficient use of cash. The 
        costs of hedging against exchange rate volatility would be vastly 
        reduced. <br>
        • An enlarged, unified GCC market, whose present GDP exceeds $300 
        billion, would offer new trading and investment opportunities. This, in 
        turn, would attract more foreign direct investment, especially from 
        multinational corporations targeting regional manufacturing and services 
