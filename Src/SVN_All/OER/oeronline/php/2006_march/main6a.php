<p align="left" dir="ltr"><b><font face="Verdana" style="font-size: 16pt">
<br>
‘Agreement on convergence criteria, a big 
hurdle’</font></b></p>
<p align="justify" dir="ltr">
<b><font face="Verdana" style="font-size: 14pt">
<img border="1" src="infouc1.jpg" width="138" height="238" align="left" hspace="5" vspace="2">A</font></b><font face="Verdana" size="1">s 
the Gulf Cooperation Council countries are moving towards a single currency, 
there are issues that remain to be resolved: the convergence criteria, location 
of the GCC central bank, pegging of the new currency etc. While there is 
political will to go forward, there are some areas that need to be looked into 
further. Clarence Michael interviewed Dubai-based Emilie Rutledge, an economist 
with the reputed Gulf Research Centre. Excerpts:<br>
<br>
Do you think all the GCC states are moving towards the single currency at the 
same speed? Are you witnessing them following the consensus convergence 
criteria? Are there any laggards?<br>
<br>
As the date for GCC currency union is still several years away it is hard at 
this point to quantatively assess the level of preparations taking place in any 
given state. Nevertheless there is no evidence to suggest marked discrepancies 
between the various states’ preparations. <br>
<br>
For instance, Kuwait’s decision to peg to the US Dollar in 2003 was billed as a 
specific step toward the establishment of a ‘Gulf Dinar’, while indirectly, the 
fact the Saudi Arabia has been substantially paying down its public debt over 
the past year or so can also be seen as a preparation. <br>
<br>
To date the process has lacked a charismatic regional leader to drive it 
forward, but since King Abdullah assumed power in Saudi Arabia it is likely that 
he will assume this role. It is widely thought that King Abdullah is fully 
committed to the process of GCC economic integration. <br>
<b><br>
Do you see any obstacle which might lead to a possible delay in the launch 
scheduled for Jan 1, 2010?</b><br>
The fact that some GCC states have pursued unilateral trade policies, and signed 
bilateral free trade agreements over the past few years has certainly 
complicated the economic integration process. For example the finalization of 
the GCC Customs Union has been deferred by two years, and will not now be 
complete until 2007. Such unilateral moves illustrate the inherent difficulty in 
cooperating as a cohesive regional economic bloc. <br>
<br>
Apart from the all-important political will, a key obstacle may turn out to be 
lack of agreement on convergence criteria. With the feel good factor resulting 
from prevailing high oil prices the will to forge ahead with complicated 
economic integration measures may be weakened. At the 26th GCC summit, held in 
Abu Dhabi in December 2005, the Finance Ministers failed to agree on the limits 
for government debt and budget deficits. Reaching a consensus on fiscal 
discipline rules is a prerequisite for the currency union and could become a 
sticking point. <br>
<br>
Nevertheless the Council’s Central Bankers were reported to have conditionally 
agreed upon a range of criteria. These include a cap on budget deficits at 3 
percent of GDP, public debt at 60 percent of GDP, inflation at the GCC average 
plus 2 percent and interest rates at the average of the lowest three states plus 
2 percent and require foreign exchange reserves to cover 4-6 months of imports.
<br>
<br>
<b>Where, do you think, can the GCC Central Bank/Monetary Authority be based and 
who can be the likely candidate head this body?</b><br>
Hosting the region’s central bank will bring with it a fair amount of prestige. 
Abu Dhabi has offered to host it, and as the UAE does not currently host any 
other GCC institution and is the council’s second largest economy, it has a 
reasonably good case. There is no doubt though that other GCC states may also 
feel they have a good case, Bahrain and Saudi Arabia in particular. <br>
<br>
The Bahrain Monetary Agency, Bahrain’s central bank, is widely seen as one of 
the regions best financial regulators and is at the forefront of developing 
Islamic financial tools. Manama also has decades of experience as an off-shore 
banking centre, last year it won several banking awards including The Banker 
magazine’s 2005 ‘Financial Centers of the Future Award’. <br>
<br>
Riyadh is a strong contender to host the GCC Central Bank as it is the capital 
of the bloc’s biggest economy (Saudi Arabia’s GDP represents 54 per cent of the 
GCC’s total GDP), and also has by far the largest population of any GCC state.
<br>
<br>
There had been talk a while back of not actually having a single GCC Central 
Bank but instead a committee of existing Central Bank governors meeting to 
decide on future policy and regulator decisions. Since consultations with the 
European Central Bank the necessity of having an independent single Central Bank 
has now been universally accepted. <br>
In the event of a successful launch of the GCC single currency, do you feel the 
dollar peg should stay? Or would the following options be considered: A 
Euro-peg? A managed float or a free float? Tied to trade-weighted basket of 
currencies? Pegged to gold? <br>
<br>
The GCC’s economies are maturing and diversifying rapidly. In light of this, I 
think that they will at some point want to consider a more independent exchange 
rate regime that will allow them to set their own monetary policy. Inflation in 
2004 and 2005 was worryingly high in some GCC states, particularly Qatar and the 
UAE, but because interest rates are tied to those of the US, monetary policy 
tools could not be used to tackle inflation. <br>
<br>
This situation is far from optimal for the GCC economies. If the GCC Finance 
Ministers decide that opting for a free float initially is too risky then I 
think they will consider a peg to a trade weighted basket of currencies which 
will include a heavy Euro component. This will make them less susceptible to 
devaluations in the dollar which has in the past often led to painful 
inflationary spikes. <br>
<b><br>
Will this lead to oil being priced in Euros and eventually in the GCC single 
currency or something else?</b><br>
The decision of whether or not to invoice oil sales in Gulf Dinars is a long 
term policy choice, but one also laden with political considerations. The 
economic benefits of such a decision would have to be weighed against the 
potential political disquiet from the US. Invoicing oil in Gulf Dinars is likely 
to result in more oil revenues being retained regionally than being recycled 
into US assets; the current petrodollar status quo. <br>
<br>
Oil importing nations would be required to hold the Gulf Dinars and this would 
increase the GCC’s seigniorage revenues. Eventually, central banks in the wider 
Middle East and in Islamic countries may choose to hold the currency as part of 
their reserves and possibly to anchor their own currencies to it. All of this 
would increase confidence in the GCC economies and would greatly improve their 
economic and political clout on the international stage. <br>
<b><br>
Will the introduction of the GCC single currency create any uncertainty in 
dollar assets?</b><br>
Whether it does or not partly depends on the oil price and GCC investment 
preferences. The US’s current account deficit is increasingly a result of the 
huge surpluses accruing to OPEC countries, when in the past it was mainly due to 
the massive surpluses of China and Japan. The GCC states investment portfolios 
are not as Dollar-biased as the Asian states, the GCC are more interested in the 
returns on their investments and interest rate differentials. With the launch of 
the GCC single currency there will be more regional investment opportunities and 
therefore we are likely to see more funds being invested locally. Therefore, the 
long term impact on the strength of the Dollar is likely to be negative, as they 
will see less of the Dollars they spend on gasoline being re-invested in their 
domestic economy. <br>
<b><br>
Do you subscribe to the view that the single currency will help attract more FDI 
to the region?</b><br>
This will undoubtedly be the case and is one of the reasons why forming a single 
currency is such an integral and important part of the region’s economic 
diversification process. Not only will a single currency make it much easier for 
foreign companies to operate seamlessly throughout the Gulf, but it will also 
send a message to the global business community that the GCC has open and 
transparent economies. <br>
<b><br>
How do you think the businesses here will adjust to the single currency? Do you 
have any suggestions for the businesses in the region? </b><br>
The Gulf Research Center carried out the first GCC wide business survey on the 
subject last year and we found that businesses were strongly in favor of a 
single currency. However, businesses expressed concerns at the lack of 
information provided by the region’s governments thus far. <br>
<br>
While 2010 may seem a long way off, preparing businesses and citizens for a 
‘Gulf Dinar’ is critical for a smooth transition to currency union. The single 
currency is a major economic issue that will affect business investment plans 
and therefore businesses need to be kept informed and prepared sooner rather 
than later. <br>
<b><br>
What about banks? Will the single currency facilitate their operations? What are 
the benefits?</b><br>
This sector will benefit. There will be increased investments in the newly 
formed currency from overseas, as was the case in the Euro zone. States and 
institutions particularly in the Arab and Islamic world’s will want to hold or 
set up Gulf Dinar-denominated portfolios.<br>
<b><br>
The EU successfully launched the single currency with 15 member states. The 
ASEAN with less member states still operate uniformly to a large extent but 
without any single currency or customs union? In light of this, how much the GCC 
states will have to carefully orchestrate and execute their plan to avoid 
pitfalls, if any?</b><br>
At the end of the day, a lot depends on whether the political will is there to 
see the process through. The EU had the political determination to achieve a 
viable currency union, even though their economies were not necessarily 
optimally suited for monetary union. Only with significant political commitment 
will the GCC be able to accept the loss of national sovereignty which is part 
and parcel of forming a fully integrated economic bloc. <br>
<b><br>
Some time ago, the GCC commissioned EU to produce a report regarding the single 
currency. Do you know what kinds of recommendations were made and whether these 
could be implemented by the GCC?</b><br>
The European Central Bank report focused on regulatory and institutional 
preparations and made many recommendations. It emphasized the importance of 
improving the standardization and transparency of statistical data, 
strengthening existing GCC institutions and the need to create a functioning GCC 
central bank, with adequate levels of autonomy, as soon as possible.<br>
It was beyond the remit of the report to assess the potential costs and benefits 
of a GCC currency union. Nevertheless it is apparent from a careful reading of 
the report that the ECB considers the GCC economies to be adequately 
synchronized for currency union and that they should benefit in various ways 
from a greater degree of economic integration. <br>
<b><br>
The US trade deficit is peaking and budget deficit is also on the rise. Federal 
debt may hit US $ 8 trillion. What are the ramifications of these on the 
greenback’s value in the near term and long term?</b><br>
Between 2002 and the end of 2004 the dollar, in broad trade weighted terms, fell 
by 15 per cent, while its decline against the euro over this period was even 
more pronounced, falling by 30 per cent. At the time analysts thought the trend 
would continue through 2005. However, last year the Dollar regained some of its 
strength against major currencies. <br>
<br>
It is widely thought that this is only a temporary recovery resulting from 
positive interest rate differentials between the Fed and the ECB. <br>
<br>
In the short term it is likely this positive US dollar/euro interest rate 
differential will be eroded as the Eurozone is likely to embark on a tightening 
cycle this year. If this takes place investors will once again start shifting 
dollar denominated savings into euro denominated ones. The structural imbalance 
on the US’ current account and government budget will not be sustainable and 
therefore the Dollar is likely to fall significantly in the long run. Several 
reasons make this scenario likely. <br>
<br>
Firstly, as a result of high oil prices there is a net transfer of current 
account surpluses from Asian countries to oil exporting nations. This means that 
Asian countries are having to draw down their Dollar reserves. Unlike in the 
past, OPEC nations are diversifying out of Dollar assets and are increasingly 
sensitive to the return on their investments. <br>
<br>
As a result, the US can no longer assume that the additional Dollars it spends 
on oil will simply be recycled back into its economy. <br>
<br>
Secondly, China has been accumulating massive Dollar assets in order to stop its 
currency from appreciating against the Dollar as it is dependent on US demand 
for its cheap exports. This will change in the long run as Chinese domestic 
demand is set to grow significantly. <br>
<br>
As a result it is likely to move towards a more market- orientated exchange rate 
removing its need to hold large Dollar assets. Consequently the dollar will have 
to devalue substantially to correct the large current account deficit.</font></p>
