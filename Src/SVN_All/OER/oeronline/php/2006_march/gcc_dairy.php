<p align="justify" dir="ltr"><font face="Verdana" style="font-size: 14pt"><b>I</b></font><font face="Verdana" size="1">nternational 
passenger and cargo operations in the Gulf region are getting bigger and bigger. 
According to the International Air Transport Association, the Middle East has 
recorded a 13.1 per cent growth rate, compared to the global rate of 7.6%.<br>
<br>
Gulf Cooperation Council (GCC) that spends more than US$12 billion on annual 
international vacations, is naturally a big market for all the airlines in the 
world. Saudi Arabia is one of the biggest outbound markets with tourists from 
the kingdom spending US$6.7 billion annually on overseas travel – this is around 
5 per cent of the country's GDP. The UAE is not far behind, with its travelers 
spending more than US$4.9 billion. Air traffic in the UAE recorded a 10.31 per 
cent jump in 2005 reaching 390,993 flights, well above the previous year's 
354,426, according to the latest government statistics. <br>
<b><br>
Want More!</b><br>
According to Boeings estimates, if the traveling activity goes like this, the 
Middle East will need 869 aircraft to meet the demand - valued at US$115.1 
billion - in the next 20 years. The Middle East's aviation industry is recording 
an above-average 5.5 per cent growth between 2000 and 2005, over the global 
average growth rate of 4.8 per cent, Boeing’s vice-president for marketing, 
Randy Baseler has said. <br>
<br>
To meet the escalating demand and for fleet expansion, airline companies have 
placed huge orders for aircraft. Emirates Airlines placed the largest order for 
Boeing aircraft to date, signing up for 42 Boeing 777 planes. These include 24 
Boeing 777-300ERs, 10 777-200LR Worldliners and eight 777 Freighters, with the 
first aircraft scheduled for delivery in 2007. Emirates earlier ordered for 43 
Airbus A380, which will enter into services next year. By 2010, new models of 
Airbus A350 will become part of Emirates fleet. Kuwait’s Jazeera Airways and 
ALAFCO (international aircraft leasing company) have also gone for the Airbus 
purchases. Jazeera Airways has so far ordered 10 Airbus A320s, while ALAFCO 
signed a contact for 12 Airbus A350s. <br>
<br>
Meanwhile, Abu Dhabi’s Etihad Airways inducted its first Boeing 777-300ER into 
its fleet recently. With the induction of 777-300ER, Etihad will become the 
first Arab carrier to offer high-speed internet and live television service on 
the board. The 777-300ER is the world's largest long-range twin-engine jetliner 
and is capable of carrying 365 passengers up to 7,880 nautical miles (14,594 
kilometers) with less fuel consumption. The inclusion of 777 will give Etihad a 
slight edge over other airliners in expanding its routes in Asia, Africa, Europe 
and North America. <br>
Saudi Arabian airlines Saudia placed an order for 15 Embraer 170 aircraft 
(66-seater) from Brazil to be used on both Gulf and domestic Saudi routes. Oman 
Air is also expanding its fleet by buying three more aircraft (both Boeing and 
Airbus are in contention) by May next year. Gulf Air is also planning to replace 
its aging nine Boeing 767s as part of its &quot;Smart Airline, Successful Business 
strategy.&quot; <br>
<b><br>
Budget Airlines</b><br>
The key problem that the Gulf aviation industry faces is that it has the highest 
average airfares in the region. To deal with the problem, most of the airliners 
are coming out with ‘Budget Airlines’ to reduce their costs and increase profit 
margins. Air Arabia and Gulf Traveler (an arm of Gulf Air), are two budget 
airlines, already in operation. Recently, Kuwait launched its first low-cost 
carrier, Jazeera Airways. Last July, National Air Services became the first 
private airline licensed to operate in competition to Saudia's domestic 
services. Another private low-cost Saudi Arabian airlines called Sama, plans to 
start its operations in the next few months.<br>
<b><br>
Cut-throat competition</b><br>
Does the Middle East have enough market for the growing number of carriers? 
Analysts say the size of the regional market is not big enough for their 
survival and growth. This became clear when Qatar pulled out of Gulf Air, which 
was established more than three decades ago by four Gulf Arab states. In 2002, 
the Abu Dhabi government also withdrew its stake, leaving only Bahrain and Oman.<br>
<br>
In the past decade, national carrier Kuwait Airways Corp. (KAC) has faced losses 
almost every year. Increasing private carriers will pose a danger to the 
existing airliners. Although not publicized, many airlines in the region 
subsidize fuel costs of their flag carriers. Fuel costs are the largest expense, 
running into hundreds of millions every year for the larger carriers. <br>
<br>
Competition is intense mainly because of a battle for passengers on intra-Gulf 
routes. Despite no shortage of airports and airliners across the region, 
availability of passenger traffic may be a problem. <br>
<br>
The aviation sector is expanding in the region with the expectation that the 
passenger flow will increase considerably to fill the carriers. This is 
unlikely, say analysts, for the simple reason that the passenger traffic is 
driven mainly by the Indian subcontinent. <br>
<br>
To survive and grow, it is necessary for all the airliners to expand their 
activities by adding new destinations to Europe, Asia and other Middle East 
countries. Instead of fragmenting their resources, GCC countries have to 
consolidate their strengths to achieve better results.<i>- Sowmya Sri 
Rallabhandi</i></font></p>

