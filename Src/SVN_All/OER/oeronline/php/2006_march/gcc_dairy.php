                        <img border="0" src="uae.jpg" width="68" height="41" align="left">UAE<br>
						Construction boom</b><br>
						In the recent past, the GCC region has witnessed a 
						greater deal of construction activities. A significant 
						part of the projects worth billions of dollars took 
						place in Dubai (UAE). The new law, that will allow 
						expatriates to own free hold property, is expected to be 
						vital factor for the growth of the construction 
						industry. <br>
						<br>
						The sector contributed 12.2 per cent to Dubai’s non-oil 
						GDP in 2004. The construction sector GDP increased by 
						166 per cent during the period 2000-2004 and has grown 
						with an annual average growth rate of 27.7 per cent 
						during the same period -- reflecting the construction 
						boom in the Emirate. The construction sector’s GDP is 
						expected to reach AED 15,985 million in 2005.<br>
						<br>
						The ambitious construction projects currently undergoing 
						and expected to complete in next 10 years include Burj 
						Dubai, Nakheel Islands, Dubai Light Rail Transport (LRT) 
						project, and Deira Palm Island. On the flip side, 
						projects worth billions of dollars have been shelved on 
						the highly publicized Dubailand development. More than 
						200 individual projects were originally planned when it 
						was launched in October 2003 but so far only 21 have 
						been signed.<br>
						<b><br>
						<img border="0" src="kuwait.jpg" width="66" height="43" align="left">Kuwait<br>
						Looming large deficit</b><br>
						Kuwait is forecasting a US$5.9 billion deficit for the 
						2006-2007 budget starting April, mainly due to a US$7 
						billion payment to the state pension agency, according 
						to the Finance Minister, Bader Al-Humaidi. Kuwait has 
						projected a deficit in each of the past six fiscal years 
						before actual figures showed a huge surplus. In an 
						interview given to official KUNA news agency, the 
						minister blamed the 20 per cent expected deficit on the 
						expenditure side on a lump sum to be paid to “settle the 
						liabilities of the Public Authority for Social 
						Security.” Revenues were projected at US$29.5 billion, a 
						staggering 88 per cent increase from US$15.7 billion 
						estimated for the current year that ends on March 31, 
						the minister said.<br>
						<br>
						Humaidhi described the budget as the largest in the 
						country since its independence. Oil revenues were 
						estimated at US$ 26.8 billion, exactly twice the 
						projected income for this year. Oil revenues were 
						estimated at the base price of US$36, he said. Oil 
						revenues represent 90.9 per cent of the expected 
						national income against 85 per cent rate in the 
						FY2005-06. The minister indicated that the non-oil 
						revenues could rise about 13 per cent over the last 
						year. The non-oil national income represents 9.1 per 
						cent of the overall revenue in the coming year against 
						15 per cent in the 2005/06 year.<br>
						<br>
						In the current fiscal, it had projected a deficit of US$ 
						9 billion, but economic reports have forecast a whopping 
						net surplus of up to US$ 23 billion. Kuwait has posted 
						revenue of US$ 34.5 billion in the first three quarters 
						of this fiscal, more than double the US$ 15.7 billion 
						projected for the whole year, according to official 
						figures. <br>
						<br>
						Commenting on the overall trend of the budget, the 
						minister said that the deficit in the 2006-07 budget was 
						mainly due to the settling, in one lump sum of the 
						liabilities of the social securities funds, instead of 
						paying the liabilities in separate installments over 
						several years.&quot; The deferred payments would have saved 
						the revenue office vast amounts of money,&quot; the minister 
						said. <br>
						<br>
						He called for using the large increase in the oil 
						revenue in backing up the national reserves and 
						investing some of them efficiently. He also stressed the 
						need for resisting the temptation of spending the excess 
						revenue on more expenditure items. <br>
						<br>
						The minister indicated that more money was earmarked in 
						the 2006/07 Budget for the fuel needed in power electric 
						generating stations and water desalination plants. He 
						said more money was also earmarked for subsidizing the 
						local gasoline market in light of the higher fuel prices 
						worldwide and subsidizing the iron and cement due to the 
						higher prices of these commodities. The subsidies were 
						needed in order to meet the requirements of the current 
						increase in the housing sector, the minister said. <br>
						<br>
						<b>
                        <img border="0" src="saudia.jpg" width="61" height="43" align="left">Saudi Arabia<br>
						Foreign assets up</b><br>
						The Saudi Arabian Monetary Agency's net foreign assets 
						rose 4.8 per cent in December to US$150 billion, up 74 
						per cent on a year earlier, according to current 
						official figures. Saudi Arabia is enjoying huge revenues 
						from crude oil exports, which have transformed the 
						economic outlook of the Kingdom. Net foreign assets in 
						December rose to 563.55 billion riyals from 537.67 
						billion riyals in November and 324.1 billion riyals in 
						December 2004 while money supply growth slowed 
						considerably in 2005 the figures show. Money supply in 
						2004 rose 19.1 per cent from 2003.<br>
						<br>
						Deposits with banks abroad, however, reached 113.9 
						billion riyals in December, down 13.2 per cent from 
						November maintaining a seesaw trend since September. 
						Deposits with banks abroad totaled 48.4 billion riyals 
						in 2004. December’s M3 money supply grew 1.1 per cent 
						from November to 546.3 billion riyals, and up by 11.4 
						percent from a year ago.<br>
						<br>
						<b>
                        <img border="0" src="qatar.jpg" width="62" height="41" align="left">Qatar<br>
						Record GDP growth</b><br>
						Qatar's gross domestic product (GDP) reached a record 
						US$34.37 billion in 2005 showing an incredible 20.8 per 
						cent growth over the previous year, according to figures 
						released by the Ministry of Economy and Commerce. <br>
						<br>
						The Ministry said preliminary indicators pointed that 
						the economy was expected to be buoyant in the 
						foreseeable future. The Minister of Economy and 
						Commerce, H.E Sheikh Mohamed bin Ahmed Al Thani, 
						attributed higher economic growth to the open policy 
						being pursued by the state. The economic growth rate was 
						likely to be maintained in future, the ministry report 
						stressed. The rate of inflation in 2005 was unlikely to 
						exceed the previous year's percentage of 6.8.<br>
						<br>
						&quot;The rate of inflation is within control. It has not 
						crossed levels that should cause any worries.&quot; As for 
						GDP, the fourth and last quarter of the year (2005) saw 
						the maximum growth at US$9.6 billion, compared to the 
						third quarter (Q3) figure of US$4.9 billion. There was a 
						huge difference in the figures of the two quarters. The 
						visionary steps taken by the Emir, H.H Sheikh Hamad bin 
						Khalifa Al Thani, has had an impact on all aspects of 
						life and the sectors of the economy were no exception, 
						he said. <br>
						<br>
						The higher economic growth was primarily a result of 
						mega projects that have been launched and the current 
						year (2006) is expected to be more promising, the report 
						pointed out, adding that the state was successful in not 
						only attracting foreign capital but also investment from 
						local sources and other Arab countries. This amply 
						demonstrates that Qatar is an attractive global and 
						regional investment destination, said the economic 
						ministry report. The GDP in 2004 was QR104 billion as 
						against QR86 billion in 2003 with the growth rate being 
						20.3 per cent. <br>
						<br>
						<b>
                        <img border="0" src="bahrain.jpg" width="39" height="26" align="left">Bahrain<br>
						Difficult to shake off</b><br>
						Despite Bahrain’s efforts to diversify its economy, its 
						dependence on the oil sector continues. The significance 
						of oil is increasing year after year. Oil continued to 
						remain the main source of country’s exports. Oil’s 
						contribution to the total exports has increased 
						gradually from 66 per cent in 2001 to 73.8 per cent in 
						2004. The total exports of Bahrain for the year 2004 
						totaled BD 2.83 billion of which oil exports input 
						totaled BD 2.08 billion. The total exports in 2004 were 
						up by 13.4 per cent to BD2.83 billion, on account of 
						both the increase in production as well as higher oil 
						prices in the year 2004. On the other hand, the total 
						non-oil exports stood at BD0.74 billion or around 26.2 
						per cent of the total exports in 2004. The non-oil 
						exports reported an increase of 0.8 per cent in 2004 and 
						hence the contribution to the total exports has been the 
						lowest since 2000. Oil also plays a significant role in 
						Bahrain’s import bill. The total oil imports during 2004 
						stood at BD1.04 billion, witnessing a sharp increase of 
						33.8 per cent in 2004. Oil imports contribution of 42.6 
						per cent to the total in 2004 is much higher than that 
						in 2003 and 2002. At the same time, the contribution of 
						non-oil imports to the total imports declined from 66.6 
						per cent in 2002 to 57.4 per cent in 2004.<br>
						<br>
						Bahrain continued to depend heavily on oil for its 
						revenue. Bahrain’s dependence on oil can be traced from 
						the fact that oil contributed 23.3 per cent (24.9 per 
						cent in 2003) of GDP and 72.6 per cent of government 
						revenue in 2004. However, any downward fluctuations in 
						oil prices would severely affect Bahrain’s economy as, 
						almost three fourth of the government revenue is still 
						generated by the oil and gas sector. <br>
						<br>
						The government’s efforts to reduce reliance on oil is 
						showing its results as the private sector GDP is 
						estimated to have grown by 15.65 per cent in nominal 
						terms in the year 2004. The real GDP of Bahrain 
						increased by 5.4 per cent in 2004, which was the second 
						highest growth recorded by the country in the past five 
						years. <br>
						<br>
						In nominal terms, Bahrain’s GDP increased by 13.6 per 
						cent in 2004 to BD4.141 billion. In 2005, the Bahraini 
						economy is expected to have shown strong growth on the 
						back of prevailing high oil prices, higher than 2004 
						average price. Real GDP growth rate for Bahrain is 
						estimated at the range of 5-6 per cent in 2005.</font></td>
