<b>Stock Brokers </b><br>
Interest in stocks and shares and the Muscat Securities Market has boomed over 
the past few years. Oman-based investors need professional advice. Indeed, the 
vast majority still rely on word-of-mouth recommendations and full or partial 
service brokers to execute trades, provide guidance and research. <br>
<br>
Stock brokers could easily launch a simple blog that tracks daily market-moving 
events, highlights research and helps investors understand the in and outs of 
stock market investing. A more informed investor is more profitable than his 
uninformed counterpart. A blog can help turn those not so well briefed investors 
into better customers, right? <br>
