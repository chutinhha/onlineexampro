<b>Education</b> <br>
Individual departments and university lectures have blogs, but I haven’t been 
able to track down an Oman-based college or university with a blog of its own.
<br>
<br>
Oman’s higher education sector is booming and colleges are competing each year 
to attract the best students. So how can colleges talk to prospective students 
as well as mums and dads? Blogs. Most colleges run a website, but these tow an 
official and fairly dry line. <br>
<br>
On the other hand, a blog with information about the college, one that could 
include news about its great business or IT departments, and student life issues 
would spice things up and give prospective students a reason to keep checking in 
with the college during the decision-making process. A blog would also provide 
current students with information on campus events, workshops, seminars, career 
fairs, deadlines for course enrolment and other relevant news. Let students have 
a separate voice and use blogs as a way to bring information directly to current 
and prospective students in a straightforward fashion. <br>
