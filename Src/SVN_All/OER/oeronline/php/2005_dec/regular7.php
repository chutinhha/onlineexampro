</font><b><font face="Verdana" size="5" color="#FF0000">
Time to blog<br>
</font></b><font face="Verdana" size="4" color="#FF0000"><br>
</font><font face="Verdana" size="1">Four years ago, blogging was a little-known 
pastime practised only by computer geeks. The idea was to make interactive 
journals available on the Internet that anyone, anywhere with a computer could 
access. Since then, the blogging trend has grown explosively. It is estimated 
40,000 of these online journals are created each day and they cover everything 
from industrial estate to climate change to the latest in Latino style, 
products, entertainment and culture and everything in between. Believe it or 
not, there are over 16 million blogs in existence.<br>
<br>
Simply put, blog is a series of updated posts on a web page in the form of a 
diary or journal, often including commentary on, and hypertext links to, other 
websites. Additions to the blog are posted in chronological order and can 
contain anything from simple text, to music, images and even streamed video.<br>
<br>
For corporates across the globe, blogs are quickly becoming the new darlings of 
interactive marketing – firms have discovered that blogs support branding and 
customer communications, create awareness and customer loyalty. Moreover, it 
offers firms an additional opportunity to sell their product or service. But how 
many Oman-based organizations are benefiting from a corporate blog? <br>
<br>
Apart from the newly-launched PEIE Mirror (http://peie.blogspot.com), a blog 
from the Public Establishment for Industrial Estates, there seem to be few Omani 
organizations tapping into the potential of blogging. In fact, there are obvious 
sectors in the Omani economy that should be developing blogs. If they did, they 
would be reaping rich rewards. <br>
<br>
<b>Radio, TV &amp; Cinema </b><br>
Oman’s radio, TV and cinema businesses are still relatively young and all rely 
on three forms of marketing: advertising, public relations and word-of-mouth. 
The latter has been the most important for all three and has contributed to the 
organic growth we're seeing in these industries. However, none of them have 
blogs and that's surprising. <br>
<br>
For cinema owners, a blog would be a great way to promote upcoming releases and 
little-known movies that are finding an audience. Radio could use blogs to 
promote programs, new music releases, events and a DJ culture. TV could use a 
blog as a marketing device to help push television scheduling on behalf of 
advertisers. The news flow in these industries is steady and blogs would be a 
great opportunity to get that information out to a growing youthful, educated 
and tech savvy Omani audience. Radio, TV and cinema are three key and important 
creative sectors and I’m surprised they haven’t embraced a cheap, interactive 
and non-resource-intensive marketing device like blogs. </font></p>

