<b>Travel</b> <br>
Today, most folk are members of frequent flier programs. But how often does your 
airline of preference talk to you? And when they do, what are they selling you? 
That’s right, knock down fares at times of the year when you either can’t or 
don’t want to travel! Let’s face it, they could be communicating a whole lot 
more often as well as whetting our appetite with news on locations, car hire, 
restaurant and hotel recommendations, sightseeing tips, airline regulations and 
flight specials would all be a hit. I’d like to see airlines humanize the travel 
experience. This would certainly foster goodwill with passengers.<br>
<br>
