<b>Supermarkets </b><br>
Contrary to popular belief, supermarkets make us richer. They’re hugely 
efficient, forcing down costs. Groceries (clothes and household goods) are today 
much cheaper than in the pre-supermarket era 20 years ago. That leaves us more 
money to spend on the things we value. Supermarkets enrich our choice too. The 
shelves groan with fruit and vegetables that were unknown 25 years ago - or 
unavailable when they were out of season. Supermarket shopping is big business, 
but why no blogs?<br>
&nbsp;<br>
For example, a food smart blog with food-related news, views and advice would be 
a good way to attract and retain customers. There could be a Q&amp;A section where 
folk ask a local chef associated with the supermarket questions on everything 
from health food to preparing a dinner party for 10. Extend this idea to include 
news about slashing your weekly food bill, cheap and easy recipes, menu planning 
ideas, shopping hints and you have a destination for food lovers, and a way to 
extend your brand and increase sales. This isn’t rocket science and in fact I 
have to say how surprised I am that apart from Marks and Spencer’s and ToysRus, 
I receive no e-mail or text contact from the&nbsp; supermarkets and larger 
stores my family and I spend our cash in. Most seem to rely on the fliers they 
put under my gate. Maybe they need to master a few Net basics before <br>
they blog?<br>
