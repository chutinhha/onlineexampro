                        <b>Muscat Wastewater Al-Ansab plant <br>
                    <br>
                    </b>His Excellency Ahmed Bin Abdulnabi Macki, the National 
                    Economy Minister, laid the foundation stone for the Muscat 
                    sewerage project at Al-Ansab. The first phase of the project 
                    will cost RO58 million, and RO31 million will be invested in 
                    the processing plant. The project is being implemented at a 
                    total cost of RO400 million. The plant will have a capacity 
                    to recycle 53,000 cubic metres per day and the treated water 
                    will be used for irrigation in the capital area. Muscat 
                    Municipality President Abdullah bin Abbas bin Ahmed, who is 
                    also the chairman of the Oman Wastewater Services Company, 
                    which is carrying out the project, said the plant was one of 
                    biggest in the world. It would adopt state-of-the-art 
                    systems in water processing to produce high quality water. 
                    Established in 2002, Oman Wastewater Services Company has 
                    master plan to serve 90 per cent of the Muscat population 
                    and its six Wilayats – Muscat, Muttrah, Baushar, A’Seeb, Al-Merat 
                    and Quriyat – by 2017.</font></p>
