                        <b>Oman signs FTA with US<br>
                    </b><br>
                    <img border="1" src="oman_fta.jpg" align="left" width="125" height="129">The 
                    United States and Oman reached a free trade agreement to 
                    eliminate tariffs and barriers and expand trade between both 
                    countries after seven months of negotiations. &quot;This is a 
                    high-quality, comprehensive free trade agreement that will 
                    contribute to economic growth and trade between both 
                    countries,” US Trade Representative Rob Portman said in a 
                    video conference with Omani Trade and Industry Minister HE 
                    Maqbool bin Ali bin Sultan. <br>
                    <br>
                    The agreement will generate export opportunities for US 
                    companies, farmers, and ranchers, help create jobs in the 
                    United States, and help American consumers save money while 
                    offering them greater choices. In addition to eliminating 
                    its tariffs on US products, Oman will provide substantial 
                    market access across its entire services regime, provide a 
                    secure, predictable legal framework for US investors 
                    operating in Oman, provide for effective enforcement of 
                    labour and environmental laws, and protect intellectual 
                    property.<br>
                    <br>
                    HE Maqbool noted that it &quot;took only seven months to 
                    negotiate this agreement,&quot; which Portman said should serve 
                    as an &quot;example&quot; for accords the United States plans to 
                    negotiate with other countries. <br>
