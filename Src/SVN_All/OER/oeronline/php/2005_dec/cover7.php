            <p align="justify"><font face="Verdana" size="1">
            <b>Saudi Arabia sails into WTO fold </b> <br>
            Saudi Arabia got the approval from all major trading nations to join 
            the World Trade Organisation (WTO) after 12 years of tough blow hot, 
            blow cold negotiations. The entry of the kingdom, which will bring 
            WTO membership to 149, is likely to open its growing economy to the 
            outside world. It is expected to boost foreign investment, providing 
            funds for diversification of the largely oil-based economy, and 
            bring new export opportunities for Saudi firms, especially in the 
            petrochemical industry. The development is also likely to encourage 
            other countries in the region to follow the suit. </font></p>
