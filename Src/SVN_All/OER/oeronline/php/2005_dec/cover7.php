            <img border="1" src="cover51.jpg" align="right" hspace="5" width="100" height="72">Convergys too comes to Middle East
            </b> <br>
            It’s not only Oracle who are finding the Middle East a lucrative 
            destination for their BPO operations. Convergys too has recently 
            made a similar call. Convergys, a global leader in 
            telecommunications billing software and professional services, 
            opened a regional office in Dubai, to support the Middle East's fast 
            growing telecommunications industry. It has a growing number of 
            clients across the Middle East including telecom network operators 
            in Jordan, Oman, Qatar and Syria.</font></p>
