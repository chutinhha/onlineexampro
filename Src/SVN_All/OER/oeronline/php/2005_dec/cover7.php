            <img border="1" src="cover49.jpg" align="left" hspace="5" vspace="2" width="120" height="80">Gulf’s first private low frill airline Jazeera Airways
            launched</b><br>
            Kuwait's Jazeera Airways, Gulf’s first private low cost airline, 
            launched its operations with its first flight to Dubai. The 
            airline’s aim is to connect to Beirut, Bahrain, Damascus and Amman 
            initially. It plans to expand its destinations to Egypt and India at 
            a later stage.</font></p>
