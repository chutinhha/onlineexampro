            <b>BankMuscat’s becomes first Omani company to issue GDR</b><br>
            BankMuscat became the first company in Oman to issue Global 
            Depositary Receipts (GDR). The bank’s GDR issue received an 
            overwhelming response from the global financial community with the 
            US$149 million GDR offering. The money raised is expected to finance 
            the bank’s domestic growth and expand its international network.</font></p>
