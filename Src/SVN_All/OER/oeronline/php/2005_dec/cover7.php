            <b>Shell Oman Marketing is the number one listed company in Oman<img border="0" src="cover53.jpg" align="right" hspace="5" width="150" height="21"></b><br>
            For the fourth year in running, Shell Oman Marketing emerged as the 
            number one listed company in the Oman Economic Review 20 rankings. 
            Despite strong competition from Al Maha Petroleum Products Marketing 
            and BankMuscat, Shell Oman maintained its leadership riding on a net 
            profit of RO5.32 million in 2004.</font></p>
