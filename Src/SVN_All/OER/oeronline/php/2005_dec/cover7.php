            <b>Launch of Zee Arabia</b><br>
            India’s popular television company, Zee Network has launched Zee 
            Arabia, an unconventional youth, music and lifestyle channel, to 
            cater to the region's 'international' yuppies and music lovers in 
            English, Arabic, and other popular Asian languages. The channel, the 
            first of Zee's customised bouquets for the Arab region, is telecast 
            across three satellites, through Nilesat, Arabsat and NSS, in a 
            free-to-air format. It is also available on the basic packages of 
            E-Vision and Pehla platform.</font></p>
