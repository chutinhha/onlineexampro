            <b>
            <img border="1" src="cover47.jpg" align="left" width="80" height="69">Intel has big plans for Middle East</b><br>
            Global chip giant, Intel has announced major investment plans in the 
            Middle East. The company is going to double its workforce in the 
            region and as part of digital transformation initiative, invest in 
            four areas including local entrepreneurship, education, digital 
            accessibility and specialised technical competencies. The company 
            has also committed to establish research and development (R&amp;D) 
            facilities in the region.</font></p>
