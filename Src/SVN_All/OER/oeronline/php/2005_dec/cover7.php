            <b>Samsung overshadows Sony’s
            brand value
            <img border="1" src="cover45.jpg" align="right" width="80" height="87"></b><br>
            Samsung has become the number one global consumer electronics brand 
            by toppling Sony from its number one position. In a survey of the 
            top 100 global brands, BusinessWeek magazine and Interbrand 
            Consultancy valued Samsung's brand at US$14.9 billion in 2005, 
            making it the world's topmost consumer electronics brand, a position 
            long held by more entrenched player Sony. Furthermore, Samsung, now 
            ranked at No. 20 among the top 100 global brands, posted the biggest 
            gain in value of any global brand with an amazing 186 per cent surge 
            over the past five years.</font></p>
