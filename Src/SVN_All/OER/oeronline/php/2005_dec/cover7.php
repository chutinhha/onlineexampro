            <b>
            <img border="1" src="cover48.jpg" align="right" width="80" height="90">Launch of kids channel by Al Jazeera</b><br>
            Al Jazeera has been carrying this image of hard news-sellers for 
            long. But there could soon be a change in that as it has come out 
            with a children's channel. The content and programmes provided by 
            the channel have been carefully designed and developed for the Arab 
            child. Promoted as the television platform with right mix of 
            education and entertainment for kids, the channel presently 
            broadcasts up to six fresh hours of innovative children's programmes 
            everyday.</font></p>
