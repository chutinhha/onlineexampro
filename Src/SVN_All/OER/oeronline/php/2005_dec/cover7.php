            <b>
            <img border="1" src="cover46.jpg" align="left" hspace="5" vspace="2" width="100" height="93">Labour reforms in UAE</b><br>
            The UAE government has introduced some far-reaching changes in its 
            labour rules pertaining to changing sponsorship. According to the 
            new rules, an employee who wants to change sponsorship and his job 
            can do so now but the permission of the employer would still be 
            required. The move is expected to trigger similar kind of labour 
            reforms in other GCC countries in the near future.</font></p>
            <hr color="#C0C0C0" size="1" width="80%" align="right">
