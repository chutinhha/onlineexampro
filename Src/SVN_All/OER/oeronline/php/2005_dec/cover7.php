            <p align="justify"><font face="Verdana" size="1">
            <b>Oman and US signed FTA</b><br>
            Oman signed a bilateral free trade agreement (FTA) with the US. 
            Concluded in record time (less than seven months), the FTA is aimed 
            to give a major boost to Sultanate’s foreign trade and economic 
            stability. Though Gulf states do business worth US$70 billion with 
            the US, the Oman-US business in 2004 was just US50 million. The FTA 
            will lead to 100 per cent duty free bilateral trade in consumer and 
            industrial products as well as across a range of agricultural goods. 
            The banking, insurance, telecommunications, express delivery and 
            construction sectors will also immensely benefit from the deal.</font></p>
            <hr color="#C0C0C0" size="1" width="80%" align="right">
            <p align="justify"><font face="Verdana" size="1"><b>
            <img border="1" src="cover56.jpg" align="left" hspace="5" width="100" height="45">World’s largest 
            Nissan showroom in Muscat</b><br>
            The world’s largest Nissan showroom was opened in Muscat this year. 
            It is owned by Suhail Bahwan Automobiles. Covering 3,367 square 
            meters, the display area can accommodate the full range of Nissan 
            and Infiniti models and their variants, with space for over 25 
            vehicles. Under Nissan’s new three-year business plan - NISSAN 
            Value-Up – the company is expected to launch 28 new models globally.</font></p>
            <hr color="#C0C0C0" size="1" width="80%" align="left">
            <p align="justify"><font face="Verdana" size="1">
            <b>
            <img border="1" src="cover57.jpg" align="right" hspace="5" width="100" height="76">Dhofar Power IPO</b><br>
            The much-publicized initial public offering (IPO) of Dhoar Power 
            Company (DPC) generated lot of excitement in the financial market. 
            The issue was oversubscribed by nearly nine times mobilizing RO87 
            million. DPC had offered 35 per cent of its shares to the public. 
            US-based PSEG holds a 46 per cent stake in the firm and the rest is 
            held by Omani investors. DPC is one of five companies owning and 
            operating power plants in the state since the government opened up 
            the sector in 1996 to foreign investors.</font></p>
            <hr color="#C0C0C0" size="1" width="80%" align="right">
            <p align="justify"><font face="Verdana" size="1">
            <b>
            Taageer hits bulls eye on MSM
            </b> <br>
            With Oman’s non-banking finance sector doing extremely well, Taageer 
            Finance successfully floated its initial public offering on the 
            Muscat Securities Market (MSM). The issue was over subscribed by 
            4.63 times. More than 10,000 investors including international 
            investors applied for the shares. Interestingly, to encourage small 
            investors, 85 per cent of the offering was reserved for applicants 
            buying less than 2000 shares.</font></p>
            <hr color="#C0C0C0" size="1" width="80%" align="left">
            <p align="justify"><font face="Verdana" size="1">
            <b>MSM at record high</b><br>
            Muscat Security Market (MSM) reached record levels this year. The 
            year started on a good note for MSM as banking and finance sector 
            typically dominated and showed considerable gains. Breaking all 
            records, MSM which started the year at below 3500 reached 5699 in 
            the last week of June before easing in the second half of the year. 
            In MSM history, 2005 will be remembered for the flotation of Omantel 
            and how this fuelled an already positive market.</font></p>
            <hr color="#C0C0C0" size="1" width="80%" align="right">
            <p align="justify"><font face="Verdana" size="1">
            <b>Oman gets Arab World Economic Freedom Award</b><br>
            The Sultanate of Oman became the joint winner of the first `Overall 
            Arab Economic Freedom Award 2005', signifying the strong economic 
            fundamentals, which have put the country on a glorious development 
            pedestal. Oman bagged one more award, the `Sound Money Award' for 
            access to sound money. This award measures, among other parameters, 
            the average annual growth of the money supply in the last five years 
            minus the average annual growth of real GDP in the last ten years. 
            The Arab Economic Freedom Awards, a total of six in all, based on 
            the Fraser Institute Economic Freedom Index, are given to 
            governments from across the Arab world in recognition of their 
            achievements in the creation of wealth through the promotion of 
            economic freedom. With regards to freedom of economies, a Canadian 
            institute declared in its annual report the Sultanate as one of the 
            top 20 countries on the international level. Within the Arab 
            countries level, the Sultanate secured the second position after the 
            UAE, based on statistical information on the 2003 economy 
            performances. The index is formed by five main factors namely the 
            volume of the public sector, the country’s legal set up, ownership 
            rights, monitory supply, commercial inter-trade freedom and loans, 
            manpower and business organisation. It is worth-mentioning that Hong 
            Kong topped the international list followed by New Zealand.</font></p>
