            <img border="1" src="cover54.jpg" align="left" hspace="5" vspace="2" width="100" height="57">Omantel-Tejari tie-up</b><br>
            To promote e-commerce in the Sultanate, Omantel through its 
            associate company Omania E-Commerce entered into a joint venture 
            Tejari Oman with leading regional e-commerce firm Tejari. The joint 
            venture is aimed at enabling both government departments and private 
            organizations to source goods and services through the on-line 
            trading hub, enhancing transparency and lowering the costs 
            associated with paper-based procurement processes. Some of the early 
            adopters of Tejari Oman's electronic procurement services are 
            expected to be the aviation, telecoms and energy sectors.</font></p>
