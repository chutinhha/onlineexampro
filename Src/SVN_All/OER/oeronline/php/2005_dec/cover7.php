            <img border="1" src="cover50.jpg" align="right" width="80" height="84">UAE increases 
            fuel prices</b><br>
            UAE hiked fuel prices by a steep 30 per cent this year. UAE became 
            the first Gulf producer to enhance the price of petrol since crude 
            prices started soaring in global markets. As per market estimates, 
            local retailers were reported losing US$1.36 million daily on the 
            previous oil prices and pressurizing the government to allow free 
            marketing of fuel. </font></p>
