            <b>
            <img border="1" src="cover52.jpg" align="left" hspace="5" width="80" height="99">Omantel IPO and historic listing on MSM</b><br>
            As part of the liberalization policy of the Sultanate, Omantel 
            successfully entered the capital market with an IPO of 225 million 
            shares at a price of RO1.280 per share, aggregating to RO288 million 
            with a face value of 100 baisas. The government divested 225 million 
            shares or 30 per cent of the company’s paid-up capital of RO75 
            million. Of which, 157.5 million shares were offered to Omani 
            individual investors, and 67.5 million shares to specified pension 
            funds and charitable organisations. Omantel share opened at the 
            Muscat Securities Market (MSM) with a brief peak at RO2.800 - 
            certainly a significant price appreciation on debut. However, at the 
            time of OER going for printing, Omantel scrip was hovering at 
            RO1.810 much to the dismay of the shareholders.</font></p>
