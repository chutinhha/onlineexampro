            <b>
            <img border="1" src="cover44.jpg" align="left" width="80" height="87">China finally revalues Yuan</b><br>
            China revalued its currency the Yuan after almost a decade. It was 
            considered common logic that an economy growing at 9 per cent cannot 
            keep such an artificial control of the currency for too long. 
            Beijing’s move from a Dollar peg to a basket of currencies was 
            widely expected. The Asian giant has gone for a deceptively smaller 
            currency revaluation – at 2.1 per cent. But since the plunge has 
            been taken after a gap of ten years, more such small measures could 
            well be on the cards. </font></p>
