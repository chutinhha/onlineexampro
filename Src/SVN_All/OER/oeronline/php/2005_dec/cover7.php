            <b>Oracle to move BPO unit from Europe to Egypt </b> <br>
            In a development marking the gradual emergence of the Middle East as 
            an important IT back office operations centre, Oracle is shifting 
            its services work from Europe to its new global support centre in 
            Egypt. Besides catering to Oracle’s customer service requirements in 
            Europe, the centre will offer tailored support for Oracle’s core 
            products including Oracle E-Business Suite and Oracle Server 
            Technologies.</font></p>
