<p align="justify" dir="ltr">
<font face="Verdana" size="1">The development of the port will generate immense scope for the local 
manufacturing industry. The size and nature of the industrial development is 
expected to contribute to the expanding base of local manufacturing for domestic 
and foreign markets, as well as a growing social infrastructure in terms of 
hotels, housing, medical and recreational facilities. Expectations are running 
high that with the establishment of Sohar and through the network of Omani 
ports, airports and roads, Oman will play an increasingly important role in both 
the regional and world trade sectors.<br>
<br>
<b>Opening new vistas </b><br>
Currently the Omani economy, like other oil-rich economies, is in a state of 
unprecedented surge. This obviously is the best time to invest in the future and 
ports like the one in Sohar are the future of the country. <br>
<br>
The government has already declared its intention to further liberalise policies 
to encourage foreign investment. Addressing a large gathering of businessmen and 
potential foreign investors assembled in Muscat recently for a conference on 
'Port of Sohar – Anchoring Success' in mid-November pointed out that the economy 
is on a roll and attributed the enthusiastic response from all around to the 
healthy investment environment in Oman. The note of confidence was unmistakable.
<br>
<br>
Dutch Premier Dr Jan Peter Balkenende sees a great future for Sohar. “Oman,&quot; he 
observed, &quot;is an outstanding investment destination for foreign investors. We 
see tremendous opportunities. According to him, the port is genuinely anchoring 
success of the development of mega gas-based industries, together with the 
advanced infrastructure facilities have entitled Sohar to be the major heavy 
industry estate in the country.<br>
<br>
<b>The booming triangle</b><br>
One phrase that has been gaining currency in the context of Sohar is “the 
booming triangle”: the Dubai-Abu Dhabi-Sohar economic triangle. As a port 
outside the waters of the Arabian Gulf on the other side of the Strait of Hormuz, 
Sohar is expected to become a feeder of supplies from around the world to both 
Dubai and Abu Dhabi. It is also noteworthy that Sohar is less than a two-hour 
drive from one of the largest free trade zones in the world - situated in the 
United Arab Emirates - and within 300 km of three of the world’s largest gas 
reserves in Qatar.<br>
<br>
It is no secret that Sohar’s industrial development depends mainly on gas 
supplied from the gas fields in Oman to create a number of heavy industries. 
Some products will be processed further into a variety of downstream 
petrochemical and metal industries. The combination of heavy and medium 
industries is expected to give rise to consistent and growing shipping traffic 
into the port in all forms: containers, bulk (dry and liquid) and break-bulk. 
Experts claim that this will automatically result in having a growing logistics 
cluster. Both Sohar and Dubai business communities would greatly benefit from 
such synergy between them.<br>
<br>
Commenting on the corridor between Sohar and Dubai, HE Maqbool says: “Both Sohar 
and Dubai have their own characteristics and are more or less complimentary to 
each other, with growing cooperation between them for the benefit of both the 
countries.” It’s not for nothing that Sohar is being billed as “the new route to 
Middle East’s growth”. Its strategic location is a key positive factor. It is 
located in the international shipping route between Europe and the Far East, 
close to the emerging markets of South Asia and Africa, coupled with attractive 
return freight rates to major destinations. With the port in place, it will make 
Sohar a very good location for energy cluster, and it would be a flagship for 
Oman and the Middle East as a first cluster. <br>
<br>
CEO of Sohar Industrial Port Company Jan H Meijer opines that its location is a 
big draw for major shipping lines as well.<br>
<br>
Sohar, therefore, appears to be heading back to the historical position it had 
enjoyed as the center of Oman’s maritime and commercial activity. </font></p>
