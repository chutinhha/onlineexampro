            <font face="Verdana" size="4" color="#FF0000">We want to share 
knowledge<br>
            <br>
            </font></b><font face="Verdana" size="1"><b>Michael P. Weitzel, 
Program Manager of DynCorp International, who also heads the Muscat American 
Business Council, is upbeat about the signing of the US-Oman FTA. In an 
exclusive interview with Mehre Alam, he talks on a host of issues, including the 
FTA’s advantages for the business community in the US and Oman. Excerpts:<br>
            </b><br>
            </font><font face="Verdana" size="3"><b>
<img border="0" src="atthehelm.jpg" align="left" hspace="5" vspace="2" width="100" height="139">A</b></font><font face="Verdana" size="1"><b>s 
Program Manager for DynCorp International, Oman, what have been the highlights?</b><br>
I have to say that working with the Omani employees, getting to know them and 
seeing how they can contribute to our operations here, has been a real highlight 
for me. DynCorp International is a logistics service and supply chain management 
firm. Becoming more involved with the Omani business community has helped me 
learn a lot about international management. <br>
<br>
I have also learned that the interaction between people here is not much 
different from that in Bahrain, Qatar, or the United States. We have hardworking 
people who are dedicated to the job they do. <br>
<br>
<b>Do you subscribe to a particular style of management, and why?</b><br>
Yes, I like ‘management by walking around!’ It helps in analyzing the people who 
do the work - getting to see what the employees are doing, listening to them, 
incorporating their ideas, and encouraging them to give us ideas – that’s the 
method I prefer. And that really is the fun part of this job for me. It’s about 
getting out and interacting with the people.<br>
<br>
<b>You have recently taken over as the President of the Muscat American Business 
Council (MABC). What do you hope to accomplish during your tenure in office?</b><br>
We would like to increase our membership. We want to get more people involved, 
and to do that we have to offer good programmes; so the goal is to raise the 
number and quality of programmes. For example, we are planning workshops. We are 
considering organizing panel discussions and bringing people in to talk about 
various issues. <br>
<br>
The most important thing you hear about today in the business world is 
knowledge. The most important way to get that is by communicating with people. 
We want to share the knowledge. That’s what we are aiming for. We want to 
achieve more variety in our activities, and to bring in more people. It’s a 
never-ending cycle - the more variety, the more people you bring in, which 
allows you to have more variety, which in turn brings in more people. We do not 
want to be known just as a lunch bunch.<br>
<br>
There must be ‘access’ to achieve success. We hope to gain through the sharing 
of knowledge, and access to decision-makers within the Omani community, and 
within the business community. There are businesses here that do not have the 
same access to decision maker as others do. There are companies here whose 
representatives have never met with or talked to the Minister of Manpower, HE 
Juma Bin Ali Bin Juma. <br>
<br>
We want to help people have that kind of access to officials so they can hear 
what the minister says, and what other business people say. And we are not 
focusing on ministers or senior government officials only. We want access to the 
implementers of the policy as well - to be able to talk with the mid-level 
manager who works in the Immigration Department.<br>
<br>
It really is an exciting time. We had a special event with the new Ambassador of 
the Sultanate to the United States. Her Excellency talked about her vision for 
her job. The meeting was really nice because, at the end of the day, there was 
no difference in where she wanted to go and where we want to go - that is, to 
increase business and trade within and between Oman and the US; and, to have 
more Americans come here. It’s a great place to invest.<br>
<br>
<b>How successful has MABC been in promoting trade ties between the US and Oman?</b><br>
It hasn’t been successful in the sense that I can quote a dollar figure, but 
where we have had success is in raising the knowledge level of the people. 
Several members in the MABC participated in meetings with US trade 
representatives for the US-Oman free trade discussions last year. We had at 
least two meetings where we talked to the whole group, and then we had a 
separate group meeting to discuss labour issues.<br>
<br>
Therefore, though we can’t quantify the success, we can tell you that we are 
pitching the MABC belief that this is good business, that this is good for 
relations between the United States and Oman, and that trade agreements do make 
a difference.<br>
<br>
<b>The United States and Oman completed a free trade agreement (FTA) recently. 
How significant is this agreement for the Omani economy, in particular, and the 
GCC economy in general?</b><br>
This free trade agreement with the US is part of President George W. Bush’s 
Middle Eastern free trade scheme, which he is dedicated to getting implemented. 
It’s like a jigsaw puzzle with just one piece of that puzzle missing. In the 
grand scheme of the GCC, it again shows that Oman is one of the leaders; Bahrain 
is the only other country in the region that has signed a free trade agreement 
with the US. <br>
<br>
The FTA with Oman was negotiated and agreed to in record time. I believe there 
was only one trip between each group, one to the US and one to Oman, before the 
draft was finalized. This is extremely significant in terms of the level of 
cooperation that was displayed. There wasn’t any bickering or arguing. I can 
tell you that the FTA will do wonders for Oman. It will also do wonders for the 
US. A lot of people don’t think the US benefits as much as the host nation does, 
but I can tell you that we (the US people) do. <br>
<br>
During our meeting with the new Omani Ambassador she told us about a New York 
supermarket and spotting Zain fruit juice. With our FTA, Americans are going to 
see more and more of that. I think it is going to be great for both countries. 
The benefits are going to be unbelievable. Just look into the other agreements 
in this part of the world – Jordan, for example. There has been a several 
hundred percent increase in the level of exports to the US. There are lots of 
companies today that are ready to send their products to the US. <br>
<br>
MABC is going to encourage the American companies to send their products here. 
If you go to the Sultan Shopping Center in Muscat, you’ll find some exclusive 
American items being sold there (brought on special order). The competitiveness 
of American products is going to eliminate this special-ness. There will soon be 
American products here regularly - without the customs, the duties, and the 
restrictions. I am really excited about the opportunities that are going to come 
up now for business communities in the US and in Oman, with the signing of the 
FTA.<br>
<br>
Lots of plans are being made. We are considering organizing seminars and having 
speakers come to talk about it. Even though people know about the FTA, and kind 
of understand what it means, the individual businessman does not yet know if his 
product qualifies to be exported to the US. And, in cases where he is confident 
about his product’s qualification for it, we would like to share the mechanics 
of the process with the American public. <br>
<b><br>
What is your view about Brand Oman as an investment destination?</b><br>
It’s an uncharted territory. There are opportunities here already, and lots of 
opportunities that people don’t know about yet. There is a huge growth 
potential. <br>
<br>
I think the Omani government is doing things right. They are not rushing 
headlong into investments in the country without planning. That’s one thing Oman 
will be known for in the US - for their good planning. That’s why you don’t see 
the skyscrapers or over-building. There is a lot of planning involved and that, 
to me, is the real value of investing in Oman. The government is planning 
smartly. The growth will be controlled and steady. It’s not going to get out of 
hand. <br>
<br>
<b>How has your experience of Oman been so far? </b><br>
I don’t want to leave! It’s a wonderful place to live. I have yet to find 
anything – with the exception of early morning traffic - that I don’t like. But 
then it reminds me of back home in Atlanta. There is nothing wrong with this 
country. It’s hard to find anything to complain about. It’s just wonderful. </font></p>

