<p align="justify" dir="ltr"><font face="Verdana" size="2" color="#C0C0C0">:: 
Leadership in Dubai Event – A Report<br>
</font><font face="Verdana" size="4" color="#FF0000">
<br>
</font><b><font face="Verdana" color="#FF0000" style="font-size: 16pt">
Global leaders take centrestage in Dubai</font></b><font face="Verdana" size="4" color="#FF0000"><br>
</font><font face="Verdana" size="4" color="#FF0000"><br>
</font><font face="Verdana" size="3"><b>
<img border="0" src="lead.jpg" align="right" hspace="5" vspace="2" width="150" height="92">D</b></font><font face="Verdana" size="1">ubai, 
termed by many as the capital of the Arab world, hosted a number of leaders from 
the global political and business community recently. In the 2-day event 
‘Leaders in Dubai’, organized by Leading Minds (a division of the leading 
business information company, Institute for International Research), the leaders 
enlightened a 2,000-strong audience on the different facets of leadership, their 
experiences as leaders and the challenges faced by the global leadership. Oman 
Economic Review was the exclusive media partner of the event from Oman. <br>
<br>
Addressing the opening session on 28 November 2005, Dr.Kenichi Ohmae, known as 
Mr.Strategy for his unique strategic skills, advocated that globalization is now 
a must for business and countries. “Local success is no longer sustainable. The 
whole world is about change. Information technology will also allow you to 
dominate even if you come from a small county. IT-based prosperity is now 
shifting power from the state,” he said.<br>
<br>
Ohmae said the key to attracting foreign direct investment (FDI) is now 
regionalization with a focus on specific industries. He warned that a 
one-size-fits-all approach to attracting FDI will no longer apply. “You can no 
longer be a Jack Of All Trades – that’s now a no-no in attracting Foreign Direct 
Investment,” he said. “Generic attraction will no longer work.”<br>
<br>
Taking the thinking of Ohmae forward, Dan Pink, the best-selling trend-spotting 
author, said that global economies will now be driven by right-brain attitudes 
where designers, creators and empathisers will be leaders because their talents 
cannot be recreated offshore or made redundant by technology.<br>
<br>
Pink said the new conceptual age means living in era of strong creativity. “This 
is an age of novelty and nuance where design has become fundamental business 
literacy,” he said. “An age when everyone is in the arts and entertainment 
business. Dubai is in the arts and entertainment business with just 8 per cent 
of its GDP coming from oil and now 17 per cent coming from tourism – and that’s 
the arts and entertainment business.” He stressed that the new conceptual age 
means driving a full brain economy “with the right side being first among 
equals” where youth would be better advised studying fine arts than striving for 
traditional MBAs.<br>
<br>
Former US President’s Bill Clinton, addressing the gathering via videocast from 
Sri Lanka where he was reviewing the Tsunami reconstruction work, stressed on 
the role of corporate leaderships in moving people from poverty to opportunity. 
He said, “The power of the public to do good was never so great.”<br>
<br>
“The power for private organizations to do good is enormous,” he said. “The 
power of private citizens to do public good is greater than ever before. So, we 
have to be optimistic, but not naïve. We live in a challenging time and one in 
which we have to bring people into the circle of opportunity.”<br>
<br>
<img border="0" src="lead1.jpg" align="left" hspace="5" vspace="2" width="150" height="92">He 
raised a very pertinent question on the issue of corporate leadership and social 
responsibility as he asked, “Can we bring the poor into the positive world of 
globalization and can our obsession with economic growth and opportunity 
accommodate and deal with environmental challenges and the long term effects of 
climate change?”<br>
<br>
On the environmental front, Clinton called for greater use of clean energies. 
“Unlike oil and coal, the more wind and solar power you use, the cheaper it 
gets,” he said. “In the Gulf states, for instance, much energy could be 
generated from wind and solar power. There will not be an end to the demand for 
oil, but clean energy has to be part of a strategy that is good for the economy 
and helps maintain stability around the world.”<br>
<br>
Focusing on the challenges ahead of the Arab world, former US Secretary of State 
Madeleine Albright pointed out that 50 per cent of people in the Middle East are 
below the age of 18 and the region has one of the fastest growing populations in 
the world. Given this, Arab economies will need to generate 100 million jobs 
over the next 10 years. Government policies alone will not be the answer. “For 
the necessary jobs to be created requires a private and public sector 
partnership with business playing the leading role,” she emphasized.<br>
<br>
She added, “The information revolution has the potential to create an enormous 
number of jobs but technology is not a substitute for leadership.” According to 
her, globalization needs to be embraced for all to benefit. “We know that 
globalization is relentless and its pace like that of a strong wind but to blame 
globalization for economic unfairness is no more useful than blaming the wind. 
It’s the job of our leaders to shape the information consensus about how best to 
guide the forces of change. We can’t continue with business as usual” she added.<br>
<br>
Arie de Geus, who was at the helm of Royal Dutch Shell for 40 years and has been 
described as the global statesman of business change, addressed the concerns of 
the corporate leadership in managing assets. “More than capital and 
infrastructure, the real asset of a company is its human capital,” he advised. 
The corporate leadership should take steps and strategize to ensure that its 
assets which leave the company at the end of the day should come back next 
morning. The human capital should be nurtured and managed effectively, said the 
ex-Shell honcho. <br>
<br>
On the concluding day, 29 November 2005, Spencer Johnson, one of the world’s 
most influential thinkers and author of the best-selling change management 
parable ‘Who Moved My Cheese?’ enthralled the audience with his simple yet 
effective formulas on how to ensure and enjoy healthier success. He harped on 
the importance of change as he said: “One must change to grow. But too much 
change is not required all the time. One must be intelligent enough to 
understand how much change is enough. One should go for the little changes which 
are very big. But one has to be cautious.”<br>
<br>
Talking about the change in corporations, he warned:” The corporate leadership 
generally tend to ignore how to effect the change. Generally, individuals don’t 
want change. The people who are farthest away from the position of power resist 
the change maximum. One must remember that the change imposed is change opposed. 
Therefore, the corporate leadership must get every individual together for 
effecting change.” <br>
<br>
Comparing the growth process in Malaysia and Dubai, Dr. Mahathir Bin Mohammed, 
former Prime Minister of Malaysia, said: “I am glad to see Dubai grow and 
growing so fast. Obviously it has a successful formula but its strategy is 
different to Malaysia. In Malaysia we want to grow by acquiring capacity. Dubai 
is growing by making use of its assets and its skills in business. You are not 
producing anything, but you are a consuming society,” he pointed out.<br>
<br>
“When a country is growing, you need to really keep track of all the data 
involved because you must be informed to assess whether you are moving too fast 
perhaps and you have to find out what the repercussions could be. You must look 
far ahead because it could be that going too fast would produce something 
lopsided – so you need to keep track of all the data, all the time.” On the 
issue of co-relation between leadership and corruption, he commented, “Power 
corrupts and absolute power corrupts absolutely – and that is what is happening 
today.” <br>
<br>
Scott Bedbury, one of the world’ s leading brand architects, responsible for 
Nike’s ‘Just Do It’ campaign and the emergence of the Starbucks brand, 
mesmerized the audience with his innovative brand strategies. He said, “We must 
not forget that perception is reality. Across markets globally, the 
commoditization is becoming a common practice with very less difference between 
the two brands.” On what makes a great brand, he said, “The great brands stir 
powerful emotions that transcend products. That’s the way to escape from the 
trap of commoditization. Technologies will come and go. But strong emotional 
ties remains there. Brands become protagonists for something timeless and 
meaningful.”<br>
Drawing heavily from his Starbucks success, he said: “Today’s what we do alone 
is not important. But how we do it is important. We knew that Starbucks coffee 
must taste good but we also knew that the brand must feel good.” <br>
<br>
<i>- Akshay Bhatnagar reports from Dubai</i></font></p>

