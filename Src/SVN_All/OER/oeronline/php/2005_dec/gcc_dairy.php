<img border="1" src="saudi_pic.jpg" align="right" width="250" height="124" hspace="5" vspace="2"><b>“Saudi Arabia is on its way to becoming the WTO's</b> 149th member, paving 
the way for a stronger multilateral trading system,” 
declared Director-General Pascal Lamy after WTO General Council formally 
concluded its negotiations with Saudi Arabia on November 11, 2005, and approved 
the accession of Saudi Arabia. After 12 years of long negotiations – the second 
largest accession period after China, which took 14 years, Saudi Arabia will 
become the 149th Member of the WTO on 11 December 2005, just in time to attend 
the organisation’s Hong Kong Conference which starts on December 13. <br>
<br>
Saudi Arabia had been negotiating its membership since July 1993. It completed 
its package of documents presenting the kingdom’s terms of accession at the 
Working Party meeting on 28 October 2005. <br>
<br>
The legal texts, which run to some 600 pages, were formally accepted by the 148 
Member Governments of the WTO at special session of the General Council. Dr. 
Hashim A. Yamani, Minister of Industry and Commerce, signed the Protocol of 
Accession with full powers, thus accepting the Protocol on behalf of the 
kingdom. “Our road to accession has been long but finally rewarding,” Saudi 
Trade and Industry Minister Hashim A Yamani said in a happy note.<br>
<br>
As a result of the negotiations, Saudi Arabia has agreed to undertake a series 
of important commitments to further liberalize its trade regime and accelerate 
its integration in the world economy, while offering a transparent and 
predictable environment for trade and foreign investment in accordance with WTO 
rules.<br>
<br>
<b>Among the commitments undertaken by Saudi Arabia are the following:</b><br>
• The WTO agreement will be applied uniformly throughout Saudi Arabia’s customs 
territory. <br>
• Saudi Arabia agreed to review a fee charged for the authentication of trade 
documents and to bring it into conformity with WTO rules within two years of 
accession. <br>
• Saudi Arabia will eliminate any non-tariff measures that cannot be justified 
under WTO rules while maintaining the right to restrict the importation and 
exportation of a certain number of goods and services in order to protect public 
morals, the life and health of the population, national security interests, etc. 
In addition, Saudi Arabia has agreed to review the list of banned imports at 
least once a year and to remove items the importation of which would not 
compromise the legitimate objectives of the kingdom. <br>
• Saudi Arabia will not maintain any export subsidies on agricultural products.
<br>
• Saudi Arabia will ensure that its producers and distributors of natural gas 
liquids (NGLs) will operate on the basis of normal commercial considerations, 
based on the full recovery of costs and a reasonable profit. <br>
• In areas such as the protection of intellectual property rights, the 
application of technical regulations and standards, as well as the protection of 
food safety and human, animal and plant life and health, Saudi Arabia will 
implement the relevant WTO Agreements in full from the date of accession (i.e. 
the Agreement on Trade-Related Aspects of Intellectual Property Rights, the 
Agreement on Technical Barriers to Trade and the Agreement on the Application of 
Sanitary and Phytosanitary Measures). <br>
<br>
Saudi Arabia too will have its share of benefits while entering into WTO: 
Firstly, it will no longer be subjected to anti-dumping measures or 
countervailing duties except within the frame work of WTO guidelnes. Secondly, 
mere entering into the trade body, Saudi will become attractive destination for 
foreign direct investment and open export opportunities for Saudi firms 
especially in the petrochemical industry.<br>
<br>
However, Saudi economists felt, the entry poses a great challenge to the Saudi 
private sector, which must get ready to function in competitive atmosphere. They 
express concern that many smaller firms are not yet geared up to wage war for 
share in an open market. &quot;Are we ready? No, we are not. Even though we have been 
negotiating for 10 years we didn't do anything to get ready,&quot; said Ihsan Bu-Hulaiga, 
a member of the Shoura Council.<br>
Foreign companies will benefit in particular from new commitments in the 
distribution, insurance, banking, and telecommunications sectors, among others.
<br>
<br>
Barely a week after joining the trade body, Saudi Arabia has already given a 
license to State Bank of India and nine other foreign banks to open their 
branches in the kingdom.<br>
Fears were also expressed about the WTO free trading rules that would limit the 
country's right to restrict imports of goods prohibited under Islam, including 
pork, alcohol and pornography.<br>
<br>
Inspite of concerns that parts of the economy are vulnerable to outside 
competition, Agriculture Minister Dr. Fahd Balghunaim maintains that overall, 
membership of the WTO will see an improvement in both services and products. 'We 
are looking forward to WTO accession with a positive outlook. Increasing 
competition is good for the national economy and for consumers.'<br>
<br>
Saudi currently has extremely restricted trade sectors which will be now opened 
to world markets and every member of the WTO. Saudi Arabia is in the course of 
concluding trade pacts with all the WTO members and has so far concluded 38 such 
agreements. <br>
<br>
But, now the major interest among the Arab countries and also among other 
countries across the world would be whether Saudi Kingdom oblige the WTO norms 
and enter into trade pact with Isreal or not. Like many other Arab states, Saudi 
Arabia has maintained a boycott on trade with Israel, along with a rejection of 
diplomatic ties. <br>
<br>
However, other Arab league countries that are part of WTO body like Egypt and 
Jordan have opened peace treaties, Oman and Qatar opened trade missions with 
Israel. The five others Morocco, Tunisia, United Arab Emirates, Bahrain and 
Kuwait have not announced an official end to the boycott and trade is all but 
non-existent.<br>
<br>
A Saudi government statement said, &quot;Upon the accession to the WTO, Saudi Arabia 
will give every member equal treatment according to the articles of the WTO 
agreements.&quot; It did not mention Israel specifically. Saudi Arabia, the last Gulf 
Cooperation Council country to join WTO, is also not hasting its decision to end 
the boycott with Israel.<br>
<br>
Whatever may be the end result, one thing is for sure, that entry of Saudi into 
WTO will open the door to economic, political and social reforms in Middle East.</font></p>

