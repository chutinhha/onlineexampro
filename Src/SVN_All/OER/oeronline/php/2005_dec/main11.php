</font><b><font face="Verdana" size="5" color="#FF0000">Making waves</font></b><font face="Verdana" size="4" color="#FF0000"><br>
<br>
</font><font face="Verdana" size="1"><b>Think big. Well, that is precisely what 
the Oman government-owned Waterfront Investments is doing by planning a billion 
dollar integrated tourist complex on its virgin beaches near the capital. Group 
Editor Ramesh Kumar takes a close look at it.</b></font></p>

<p align="justify" dir="ltr"><font face="Verdana" size="3"><b>
<img border="0" src="make_wave.jpg" align="right" hspace="5" vspace="2" width="150" height="215">C</b></font><font face="Verdana" size="1">ome 
2013 A.D a grandiose plan conceived by the government and their strategic 
partners to turn Oman into a truly global traveler’s destination will turn into 
reality. A mammoth project indeed. Consider this: Total area of the project: 
2,500,000 square metres 7 kilometres of natural beach! 18-hole signature golf 
course 300 berth marina and there is much more on this list…<br>
<br>
The Wave’ is promoted by three partners: Waterfront Investments, representing 
the Omani government, the National Investment Funds Company representing the 
Omani pension funds and state general reserve funds and Majid Al Futtaim 
Investments of the<br>
United Arab Emirates.<br>
<br>
“The project has its origins when the coastal road was constructed,” recalls 
acting Chief Executive Officer Wael Ahmed Al Lawati, seated comfortably in the 
conference room of the onsite sales office. Muscat Municipality decided to look 
at something a bit more unique - turning into another real development. There 
was a greater realization that the involvement of big players is a necessity to 
give a boost to Oman tourism.<br>
<br>
Once the fire was lit around 2001, taking the project from the conceptual was 
not too difficult. Steering committees were set up, looking at various options. 
A master plan needed to be put in place. Designers competed against each other 
etching the dream project - they came up with a concept of a golf course, 
marina, houses, and hotels and commercial spaces. According to Lawati, the 
discussions reached a stage where the participants strongly felt that having a 
private sector company to take it forward would be more effective.<br>
<br>
Actual work started in 2003. “We promoted the project in a very open and 
transparent manner. We opened the opportunity to local investors. And also spoke 
to various international developers. Ultimately we got solid deals from these 
potential partners, purely on merits,” adds Amer Mustafa Al Fadhil, Vice 
President (ExternalAffairs).<br>
<br>
What went into choosing the right partners? “It was a pleasant challenge,” 
remarks Lawati. Two years ago, there was no ministry of tourism for coordination 
purposes. Nonetheless, Oman’s seriousness to go ahead with the global size 
project was conveyed to investors Out of eight competitors, one was selected 
while some of the others moved to other developmental proj-ects in Oman.<br>
<br>
The progress was not without hiccups. Admits Lawati: “The challenge was to keep 
ourselves motivated throughout and not get upset by negative remarks.” The whole 
process took approximately four months from the launching of the project to the 
point of<br>
receiving bids. Another month to evaluate, clarify, shortlist and decide. “So, a 
relatively short time for a billion dollar project to secure the major initial 
funding,” proclaims the proud Lawati, the son of a former diplomat and an 
ex-Shell engineer.<br>
<br>
The government is serious about tourism promotion. “We are aware of the 
challenges of international tourism market,” avers Her Excellency Dr Rajha Bint 
Abdul Ameer Ali, Minister of Tourism. Her efforts are concentrated on three 
areas: quality of products and services on offer, top notch global marketing 
strategy and opening up the tourism sector for direct foreign investment. The 
Wave will be one of the pioneering projects that will perhaps be a litmus test.<br>
<br>
Marketing of The Wave is expected to pick up soon. How? According to Fadhil, no 
foreigner or expat is permitted to own property in Oman. But that is expected to 
change with the land ownership statute. Sean Angle, Resident Partner of Trowers 
and Hamlins points to the proclamation of Royal Decree 21/2004 introducing land 
holding for GCC citizens and GCC corporate entities. Another piece of 
legislative action is the introduction<br>
of Foreign Ownership of Land Law (FOLL).<br>
<br>
Adds Angle: “Under FOLL, property can only be acquired by foreign entities in 
‘Integrated Tourism Complexes’ and is then subject to a number of limitations 
regarding construction<br>
and sale. As FOLL was not passed in the form of a Royal Decree, there are some 
confusions as to its enforceability.” Fadhil concedes this lacuna, but clarifies 
that the remedy is around the corner with the Majlis putting their seal of 
approval and forwarding the same to His Majesty for the proclamation of yet 
another Royal Decree bestowing legal rights on expats’ landownership.<br>
<br>
<br>
The crucial issue is: whether the purchase of property in Oman by expats 
automatically entitles them to resident visa; and does the sale of such foreign 
owned property automatically lead to the cancellation of resident visa of expats? 
Fadhil is unperturbed by<br>
these minor hiccups. “These are all part of our business and we will overcome 
them as we progress. After all, our goal is to promote tourism and boost the 
Omani economy by leveraging its natural beauty.</font></p>
