    <p style="margin: 5"><font face="Verdana" size="1"><b>The Engineering 
    Challenges</b><br>
    <br>
    The project area comprises approximately 200 hectares of existing beachfront 
    land to which the 65 hectares of new land will be added by reclamation from 
    the sea. Determined to respect the environment and avoid the significant 
    impact on the local community of transporting thousands of loads of sand and 
    rock fill to the project by road, we plan to dredge sand from the sea bed 
    and use it for most of the land filling. <br>
    <br>
    Specialist site investigations and marine engineering designs are being 
    undertaken to optimize the extent of seashore protection needed to create 
    the new land which will be protected from the sea by rock armouring. 
    Internationally qualified dredging contractors with experience of working in 
    the Middle East have been short-listed to tender for this work. Careful 
    environmental monitoring will be carried out during the dredging and 
    reclamation phase.</font></p>
    <p style="margin: 5"><font face="Verdana" size="1">The project will create a 
    new harbour by building a concrete sea wall and this will be made from 
    massive concrete blocks that will be manufactured on site. These blocks will 
    weigh around 60 tonnes and specialist equipment will be employed in their 
    manufacture and placement.<br>
    <br>
    An 18-hole international golf course is planned with full beachfront views. 
    This presents a challenge in the selection, installation and maintenance of 
    the golf course grasses and a skilled international course designer, who 
    will carefully select saline-tolerant grasses, has been employed.<br>
    <br>
    Whilst some of the new residences will have beach- or harbour- views, others 
    will be located along newly created inland waterways. As these waterways are 
    not intended to be tidal, they provide an interesting engineering challenge 
    to ensure that water levels are controlled and regular flushing of the water 
    body is achieved to optimize the marine ecology and water cleanliness.<br>
    <br>
    Careful coordination between the Developer and the Government is necessary 
    in order to ensure that the provision of all the Utilities (power, water, 
    drainage and telecommunications) services are achieved to suit the demands 
    of the project. The project will be developed in phases over 6 years and 
    this puts an extra burden on the design of utilities within the scheme 
    designers to ensure that each successive phase of Utilities distribution is 
    brought smoothly on line without disrupting the existing users. In the 
    design of these Utilities, the denser portions of the scheme are being 
    considered as potential recipients of centralized district cooling. </font>
