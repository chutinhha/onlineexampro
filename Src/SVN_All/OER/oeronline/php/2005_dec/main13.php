<p align="justify" dir="ltr"><font face="Verdana" size="2" color="#C0C0C0">:: 
Smart Manufacturing<br>
</font><font face="Verdana" size="4" color="#FF0000">
<br>
</font><b><font face="Verdana" size="5" color="#FF0000">‘Niche Manufact uring to 
Thrive’</font></b><font face="Verdana" size="4" color="#FF0000"><br>
</font><font face="Verdana" size="1">
<br>
</font><font face="Verdana" size="3">
<b>
<img border="0" src="smfac.jpg" align="right" hspace="5" vspace="2" width="120" height="147">C</b></font><font face="Verdana" size="1">ome 
January 23 - 25, 2006 at the Muscat Inter-Continental Hotel, Mark Eaton will 
present on Lean Enterprise at PEIE’s annual Smart Manufacturing Conference 
(http://peie.blog-spot.com). In this in-depth, no holds barred interview, Eaton, 
a veteran design engineer, and former Director of the UK’s Department of Trade 
and Industry’s Manufacturing Advisory Service, shares with us his thoughts on a 
range of manufacturing issues.<br>
<br>
<b>How do you see manufacturing changing over the next 20 years?</b><br>
Obviously, it is now possible to theorise about the worldwide impact of the 
growth in the Chinese economy and the stress it will place on global 
competition. I would also anticipate the continuing rise in the importance of 
brands in high-end manufacturing, both at the business and national level. I 
foresee the consolidation of low-end manufacturing into fewer, more specialised 
manufacturers who will offer flexibility and extremely high levels of 
productivity. <br>
<br>
I also predict that increasing globalisation will have the impact of increasing 
the number of Small and Medium Enterprises (SMEs) who chase low costs around the 
world, but are currently prohibited by the extreme costs involved. Within a 
little more than 20 years, I anticipate we will need to find new ‘emerging 
markets’ as the costs in China, India, even Eastern Europe will begin to reach a 
level where it is uneconomical to outsource to them. Throughout this period, 
niche businesses will continue to thrive by providing differentiated, flexible 
services at higher costs.<br>
<br>
<b>Which emerging technology do you think has the most potential to change 
manufacturing?</b><br>
You cannot answer a question like this without first referring to the impact of 
the wireless communications and the continuing growth in the Internet, on all 
aspects of manufacturing - from machine measurement through to supply chain 
planning. A secondary technology that will have an impact will be RFID (radio 
frequency identification) with its applications in everything from product 
tracking to national security.<br>
In terms of the new sectors that will impact on manufacturing, these are 
unequivocally micro/nano technology and bio-processing, all of which will make a 
major impact on the world. Due to the investment barriers to entry in the 
market, these will make it difficult for companies to break into in the medium 
term unless they consider their entry strategy in the very near future.<br>
<br>
<b>What about the issue of knowledge transfer from academia to industry?</b><br>
The amount of research capability in academia far outstrips that is available in 
industry and there is an urgent need in many economies to make this research 
more accessible to industry; also to significantly simplify the process of 
working with academia. I have been deeply impressed by the Fraunhofer Institutes 
(http://www.iis.fraunhofer.de/ index.html) that originated in Germany and how 
they are both accessible to industry and focused on the commercialisation of 
research.<br>
<br>
<b>Should universities become more responsive to the needs of manufacturing?</b><br>
I would agree that for many manufacturers the processes of accessing and then 
forming a relationship with universities has been very difficult - often caused 
by the different needs of the organisations concerned. There have been a number 
of attempts to simplify the process of getting access to universities and making 
them more responsive, including introducing layers of intermediaries. But there 
are still some major problems to overcome before universities are as responsive 
as they need to be to support industry effectively.<br>
<br>
<b>
<img border="0" src="smfac1.jpg" align="left" hspace="5" vspace="2" width="150" height="182">What 
type of involvement would you like to see manufacturers have with universities?</b><br>
Universities are fundamentally clusters of knowledge. Virtually no manufacturer 
could afford to have research capabilities as big as those held within even a 
single university. I would be delighted to see universities starting to become 
an integral, but outsourced, part of the R&amp;D functions of organisations. I don’t 
just mean large manufacturers. This may mean that different funding models need 
to be used such as deferred payments or universities taking a percentage of 
future sales without requiring funding upfront.<br>
<br>
<b>What are the common principles that guide the best manufacturers?</b><br>
The most prevalent measures of success in manufacturers around the world appear 
to be having an appropriate culture that supports the aims of the business: 
whether that is to be the most innovative, lowest cost, fastest turn-round etc. 
For many manufacturers, relationships with customers and suppliers are also key 
and the formation of effective partnerships is going to be a major source of 
competitive advantage in the coming years. In addition to these internal factors 
and skills, governmental policy and regional trading conditions need to be 
monitored and reacted too accordingly and the best organisations are adept at 
keeping close to the external factors that affect the internal operations of 
their business.<br>
<br>
<b>Anybody can buy the latest, greatest equipment, but manufacturing isn’t a 
purely mechanical system. It's the &quot;people systems&quot; that determine a plant's 
productivity. How does a manufacturer build the necessary “people systems”?</b><br>
For all but the most automated factories, people are the essential variable that 
will make every product, fix every problem and identify every improvement. 
Without ‘people engagement’ no business can hope to be a success. But how you 
engage them is recognised as one of the most difficult issues faced by senior 
management and changing culture one of the most difficult and time-consuming 
change processes. There is no general solution to how you build the necessary 
people systems. It is a function of where the business culture and systems are 
today and where the business managers want business to move to and then planning 
the improvement roadmap. Wherever the organisation starts from the solution will 
include such things as policies, procedures, management style, measurements, 
communication and reward/recognition systems.<br>
<br>
<b>We all think that the faster a plant can make a quality product, the less 
that product costs to produce. Those savings can be applied to new features. 
That's how you become more competitive, sell more products, increase profits and 
ultimately improve job security. Is this how manufacturing works in the real 
world?</b><br>
This is a common misconception and the cause of many businesses going out of 
business. Doing things faster often implies throwing extra effort and resources 
at it and these carry extra costs. In addition, making things faster that you 
have not - or cannot - sell means wasting money faster too. Going ‘faster’ often 
drives people to build bigger batches of products to avoid costly set-up times 
which would slow things down. This means producing things that cannot be sold 
immediately which ties up significant cash and space. A better approach is to 
focus on shortening change-over times so you can run smaller batches and remove 
non-value adding activities so the business is more responsive. The benefit is 
that your productivity and flexibility will increase making it easier to cope 
with what the world will throw at you.<br>
<br>
<b>Dell hit in excess of US$45 billion in annual revenue and is growing at 
nearly 20 per cent yearly. It seems well on its way toward surpassing its goal 
of US$60 billion within the next few years. To get better faster, it intends to 
slash US$2 billion in costs. It is indicated that much of the cuts will come 
from manufacturing operations and the supply chain. Is speed the ultimate 
competitive weapon?</b><br>
Responsiveness is an effective weapon for many manufacturers, but not all. It is 
important for manufacturers to understand the key drivers in their markets – 
what are the market leaders doing? What do the customers value? The best weapon 
is to have a clear understanding of the competitive factors that drive the 
sector each company operates in – looking externally at what is required rather 
than internally at what is offered!<br>
<br>
<b>Do small manufacturers need big-name supporters?</b><br>
Many small manufacturers have difficulty managing larger suppliers in their 
supply chain and I believe there will be a continuing move toward purchasing 
clusters to allow smaller companies to leverage cost savings and improvements in 
responsiveness from larger suppliers. In wider terms, many smaller companies can 
be highly successful without big name supporters, especially in niche markets or 
for markets where the customers are ‘brand insensitive’ - as in they are not 
worried by brand value. Perhaps, the best big name supporters of smaller 
companies will be banks and the media and for many these should be the ones to 
ensure you have a good relationship with.<br>
<br>
<b>Whether you're running a manufacturing outfit with 3 or 3,000 people, you 
have to hire the best engineers, the best marketers and the best production 
workers. The products you make, the programs you have, the mission you espouse 
should make people feel good about working for you. In your view, are corporate 
values a real motivator?</b><br>
In the first instance, I would like to challenge the assumption that you always 
need to hire the best, implying the most expensive. Misquoting Toyota, we get; 
“we produce excellent results from average people with excellent processes. Our 
competitors get average results, with excellent people and broken processes.” 
This statement says that a well organised (and lean) manufacturer will 
inherently be more effective than an organisation with any number of excellent 
people but with ineffective processes. <br>
<br>
Answering the second part of the question, in a competitive situation where 
manufacturers are trying to attract talent and there are numerous options for 
the individual, then the decisions come down to which organisation best meets 
the personal needs of the individual. Some of these needs will be logistical 
(closeness to where they want to live, working hours that fit their lifestyle 
needs etc) and some will be emotional (values of the business, working 
environment, appropriate opportunities for advancement etc). The most effective 
manufacturers will aim to achieve the best fit between the needs of the business 
and the logistical and emotional fit of the individual.<br>
<br>
<b>What do you do when the business world as you've always known it simply 
ceases to be? When new competitors and new technologies explode the industry 
economics? When everything that worked before won't work - and can't work ever 
again? </b><br>
It is difficult for manufacturers to recover from a major fracture in the market 
and this is where there is a need to have good external scanning processes to 
try to predict major changes in the market in a timely manner. However, using a 
metaphor, whilst we have the technology to spot large asteroids that are likely 
to hit the Earth, there is a chance we will miss one, and one is all it takes! 
Therefore, in a market where the world has changed, the winners will be those 
who can evolve fastest, the most innovative manufacturers with the ability to 
quickly restructure their finances and operations to meet the new challenge.<br>
<br>
<b>If we don't have to own it, let's not own it. And if we do have to own it, 
let's reduce the risk by sharing it. How does this kind of thinking apply to 
today’s manufacturing sector? </b><br>
There have been attempts to share resources and workforces (particularly in 
areas where the skills are difficult to access) and there are manufacturers who 
have recognised that one company’s waste is another’s raw material but these are 
in a minority at present. For many companies, forming trading partnerships to 
share resources will be an effective way of reducing risk, but the key issue is 
finding the right partner to link with. In the UK, this is being addressed by 
PSL (Partnership Sourcing Limited - http://www.pslcbi.com) who are a not for 
profit orgnisation originally established by the UK government and who are now 
leading the development of the new ISO standard (11000) which will cover how to 
effectively partner.<br>
<br>
<b>If we can't dominate a category, let's get out. Is this smart thinking? </b>
<br>
The answer to this is to look at the Boston Consulting Group matrix. If the 
market is low growth and you hold a low share (what is termed a ‘Dog’), then it 
may be best to plan an exit strategy, but if the market is growing and the 
company holds a low share (a ‘Question Mark’), then the organisation has options 
based on the competitive situation in the market. If the market looks likely to 
consolidate around one or two major players then it prompts one exit strategy 
(possibly holding out to be bought out). Lastly, if the market looks like it 
will support numerous suppliers, it may be prudent to hold your nerve and stay 
with it. The answer therefore is not simple and will need to be thought through 
by the business. </font></p>

