</b><br>
<img border="0" src="m_class.jpg" align="left" width="225" height="102">The 
all-new Mercedes-Benz M-Class is one of the first SUVs to be awarded the maximum 
five-star rating for both the frontal and side crashes in the US-NCAP (New Car 
Assessment Program) tests run by the National Highway Traffic Safety 
Administration.<br>
<br>
Mercedes-Benz won this top honour after having been highly successful in 
numerous safety tests covering all vehicle categories across the globe. In the 
American version of the NCAP test, the vehicles are subjected to a front-end 
crash at 56 km/hour against a rigid wall. The SINCAP test (Side Impact New Car 
Assessment Program) specifies an impact by a 1,368-kilogram deformable barrier 
against the side of the car on a level with the front-seat occupants at 62 
km/hour.<br>
<br>
The Mercedes off-roader sets new standards particularly with regard to passive 
safety. The unitized body with its high-strength passenger compartment and 
generous deformation zones front and rear provides the basic foundation for 
outstanding occupant protection. Adaptive two-stage front airbags for the driver 
and front passenger, front sidebags, windowbags, belt tensioners and belt force 
limiters are all standard specification.</font></p><hr width="70%" size="1">
