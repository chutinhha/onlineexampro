<b><br>
    Oman as an investment destination for South Africa</b><br>
    Like Oman, we are also trying to attract foreign investments. There are many 
    areas where we (South Africa and Oman) can look at synergies. For example, on 
the Sohar project, particularly on the downstream industries - the plastic 
sector, ferrous and non-ferrous metals etc. Back home, we do have a lot of small 
companies that are looking at expanding their capabilities and outreach. As we 
discuss and build these partnerships, we may be able to unlock the real value of 
mutual investment opportunities. <br>
    <br>
We saw the scope of investment by the Omani government alone through its 
state-owned subsidiaries and the commitment to look at over US$10billion worth 
of investments in Sohar alone. The excellent work that is being done to attract 
foreign investors augurs well for the economy. Last, but not the least, the 
