a few moments more and he gives you the impression of being a man with a 
mission. But then, one can fairly well understand his zeal. Kekana believes he 
has a job to do: that of extending his country’s growing clout and goodwill on 
the international scene. In Muscat, while on his first visit to the Sultanate, 
to attend the two-day conference on the investment opportunities in Sohar, he 
took out time to talk to Mehre Alam on a wide range of issues concerning South 
Africa and Oman. Excerpts: </font></b></p>

