<p align="justify"><font face="Verdana" size="1">
More importantly, the strategic location of Sohar is one of the key areas. For 
us, in South Africa, it offers an excellent example, which we believe may be 
complementary to Sohar in terms of movement of goods from the southern 
sub-continent. The Sohar developmental plan gave us an opportunity to study a 
similar project which we are currently engaged in. We too are embarking on 
economic development zones, similar to the one in Sohar, but not as big. <br>
<br>
We have the Coega development project in the Eastern Cape Province, which is a 
port infrastructure on the same lines of Sohar, and we have the Port of Durban - 
the largest port in Africa. It’s these complimentary movements that we are 
looking at. We are looking at opportunities of placing the Port of Durban as an 
important oil and gas and maritime hub, particularly for ship repairs, and oil 
and gas exploration opportunities. We are also looking at placing Durban as the 
supply hub, as well as the hub for docking (for refueling) and other maritime 
requirements that may arise. Foreign partners are most welcome.<br>
What has really excited us about Sohar is the pace and strategy with which it 
has attracted tenants. It has done it in such a short span of time. We think we 
can learn a lot on that. It is a role model to emulate. <br>
<br>
<b>Two-way tourist promotion</b><br>
There is a lot of opportunity in getting Omanis to visit South Africa. 
Nevertheless, last year (2004), we had only 207 Omani tourists. When we spoke to 
a few of them, they said that they found the experience very exciting. They 
enjoyed the cultural, political and environmental tourism. I think it’s a big 
challenge for us to give a fillip to Oman-South Africa tourism. The dominant 
view is that a good tourist base will spur our national carrier to look into the 
possibility of returning to this region. <br>
<br>
One of the funny things of flying to Oman from South Africa is that you fly over 
Salalah en route to Dubai, and then fly back into Oman. It costs you an 
additional three hours. In these times of global economy you don’t need such 
inefficiencies. But it’s probably an opportunity for the South African and Omani 
governments, as well as the tourist agencies, to dwell on the issue and chalk 
out something. For South Africans, it would be really worthwhile to market Oman 
as a destination. We can particularly look into the exciting challenges in 
infrastructure development, economy’s diversification and beneficiation 
strategies.<br>
<br>
Beyond the potential of merely increasing the capacity for tourism, there are 
also synergies in terms of the development of aspects of tourism outlets. We 
have dabbled into the question of developing areas to produce eco-tourism and 
user-friendly places that still maintain the raw material that we work with. 
This is something where we could perhaps make a contribution as Oman is right 
now in the beginning of setting up all those kinds of places. <br>
<br>
<b>Interests convergence</b><br>
Both South Africans and Omanis are very passionate about their countries. Both 
face similar sort of challenges in improving technical skills. Like Omanisation 
here, there is a big drive currently underway in South Africa of educating our 
black population and improving their technical skills. There is commitment to 
Islam in South Africa and we have a strong Muslim community, which is very 
supportive of the government’s relations with the Middle East; and then, both 
countries have great commitment to projects involving housing, infrastructure, 
water, sanitation and electrification.<br>
<br>
<b>The big Omani attraction</b><br>
The Sultanate’s stable economy; its government’s commitment to share in the 
risks in terms of investments along with the foreign partners, and sound trade 
and investment policies in place. We find all this very attractive.<br>
<br>
<b>Bilateral relations</b><br>
Former South African President Nelson Mandela had visited Oman in 1999. Since 
then, several senior cabinet ministers have visited the Sultanate. Last 
December, we opened the full-fledged South African embassy in Muscat. We also 
have the Omani representative in Pretoria. <br>
<br>
In terms of the status of the bilateral relations, we have already concluded two 
bilateral agreements, namely the Military Cooperation Agreement and the 
Agreement on the Avoidance of Double Taxation – both signed way back in 2002. 
Six more agreements are being currently negotiated - including one on 
cooperation on art and culture. In fact, one of our artists was recently invited 
to adjudicate on an art competition in Oman; she also had an exhibition 
organized at the Al Madina gallery. <br>
<br>
Deliberations are also underway on an agreement to establish a partnership 
forum, a bilateral air services agreement to boost tourism, a bilateral trade 
agreement, a bilateral investment agreement, a science and technology 
cooperation agreement and of course, the agreement on merchant shipping and 
related matters. These are exciting times indeed. </font></p>

