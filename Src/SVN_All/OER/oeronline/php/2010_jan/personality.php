		        <td valign="top" width="100%"> <p align="justify" style="margin: 3px 10px" dir="ltr"><font face="Verdana" size="1"> 
                    <br>
                    </font><font size="2" face="Verdana"><b>Understanding 
				different cultures is very important<br>
				<br>
				</b>
				</font><font face="Verdana" size="1">
				<b>Abdul Rahman Busaidy is probably the only Omani, who looked 
				east by landing a top position at Jet Airways, an Indian 
				company. But then boundaries have never been a barrier for him, 
				writes Visvas Paul D Karra</b><br>
				<br>
				<img src="images/personality_img1_jan2010.jpg" width="150" height="150" hspace="9" vspace="5" border="0" align="left">When 
				Abdul Rahman Busaidy, then Assistant Vice President Marketing 
				and Planning with Gulf Air which was at its peak at the time, 
				was asked to head the feasibility study for establishing a new 
				airline in India in 1992, little did he think that one day he 
				would be part of that airline heading its Gulf office. The 
				airline in question, which took to the Indian skies one year 
				later in 1993 and has proved to be the numero uno private 
				carrier in India, is, as guessed rightly, Jet Airways. As it 
				turned out, once the feasibility study was conducted and Jet 
				Airways became operational with Gulf Air and Kuwait Airways as 
				partners, Busaidi was asked to continue as board member of the 
				Indian carrier for three years until 1996, when the government 
				of India decided to stop foreign partnerships in Indian 
				airlines. </font>
				<p align="justify" style="margin: 3px 10px" dir="ltr">
				<font face="Verdana" size="1">
				<br>
				But as luck would have it, nearly a decade later, when Busaidy 
				was looking for growth in his career, Jet Airways became a 
				natural choice and his earlier credentials with the airline 
				helped him to become the Group Executive Officer, heading its 
				Middle East operations, from its base in Muscat.<br>
				<br>
				<b>Interesting period</b><br>
				Busaidy began his career with Gulf Air in 1981, after obtaining 
				an MBA degree in aviation management from Embry Riddle 
				Aeronautical University in the US and a degree from Scottish 
				University. It was interesting period at that time for airlines 
				and more so for Gulf Air which was in its hey days as the 
				leading regional carrier, and grew significantly before the 
				change of ownerships reduced the strength of that airline.<br>
				<br>
				“The first day I walked into Gulf Air, I wanted to become the 
				CEO of that company. Of course, it did not exactly happen that 
				way because I got to become the CEO of Oman Air. But it is the 
				aim and motivation that matters more. And it is not just a 
				question of dreaming, you have to work hard to get there. And 
				your education, commitment, up-to-date general knowledge and 
				above all the ability to do teamwork will help you,” says 
				51-year-old Busaidy, a man who has held top positions in some 
				the biggest airlines in the region.<br>
				<br>
				“One has to look and think beyond the boundaries of a country. I 
				went to India and joined an Indian company. The world is getting 
				smaller and Oman is no less a multinational country than any 
				other. Understanding the cultures of various countries is also 
				very important,” adds Busaidy while saying that these are the 
				traits that the Omani youth should adopt if they want to make a 
				mark in the international arena.<br>
				<br>
				<b>Rising through the ranks</b><br>
				As Gulf Air charted an upward growth path, so did Busaidy’s 
				career graph and soon he was heading the marketing and planning 
				of the carrier in Bahrain. After nearly two decades with Gulf 
				Air, in February 2000, Busaidy got an opportunity to head Oman 
				Air as its CEO. Joining Oman Air was like a dream come true for 
				him, but it was not all a bed of roses as the situation then was 
				very different from what it is now. The Sultanate of Oman was 
				having two national airlines (Oman was a joint owner of Gulf Air 
				till 2006); the capital of Oman Air was small, just 12mn rials, 
				and it was quite a challenge to prosper and grow the smaller 
				Oman Air against the giant Gulf Air.<br>
				<br>
				Busaidy rose to the challenge and with the help of the great 
				teamwork from his colleagues, they identified niche markets and 
				gaps not served by Gulf Air from Oman and serviced those routes. 
				At the same time, the government also pumped in more capital 
				into the airline. Busaidy also undertook a fleet rationalisation 
				programme, from five different types of aircraft to just two 
				(Boeing 737s and ATRs), in order to cut down on operational 
				costs and improve the efficiency and agility of the fleet from 7 
				hours flying time to 12 hours. He also got approval to buy and 
				lease aircraft. Thus, the fleet rationalisation and 
				rationalisation of fleet utilisation helped the airline to 
				introduce new routes.<br>
				<br>
				Simultaneously, Busaidy also invested heavily in automation of 
				the systems and instituted training programmes for Omani 
				graduates and brought in Omanis in management positions. One of 
				them brought by Busaidy was the late CEO of Oman Air, Ziad Al 
				Haremi, who at the time was working in Gulf Air.<br>
				<br>
				Busaidy is proud to say that talented persons like the late Ziad 
				and other Omanis were recruited by him to raise the Omanisation 
				level of Oman Air to 75 per cent in a five-year period. While 
				working as CEO of Oman Air, Busaidy was a board member in 
				several companies like Muscat Private Hospital, Oman Fibre 
				Optic, Oman Emirates Investment Company, Oman Fisheries Company, 
				Oman Chamber of Commerce and Industry and Muscat Duty Free. In 
				Bahrain, he was also a board member of Airport Handling Company.<br>
				<b><br>
				Forward-looking</b><br>
				<img src="images/personality_img2_jan2010.jpg" width="150" height="63" hspace="9" vspace="5" border="0" align="left">His 
				foresight and keen market acumen also spurred Busaidy to ask the 
				government to invest in planes, knowing fully well that Oman’s 
				tourism sector was rapidly developing and the demand for flights 
				could soar. He proposed to have long haul routes to Europe and 
				the Far East. Busaidy says that his plans were not accepted by 
				the Oman government at that time probably because it was 
				difficult to align political and commercial objectives of the 
				airline then, but it is only now that they are becoming a 
				reality. And although, he is no longer with Oman Air, he is 
				immensely pleased that it was he who had laid the groundwork for 
				the growth that Oman Air is witnessing today.<br>
				<br>
				Even as Oman Air was finding its foothold in the region, a new 
				wave of liberalisation was on in neighbouring India and Naresh 
				Goyal, the founder of Jet Airways approached Busaidy to join the 
				team in 2006. It was a challenging and stimulating period for 
				him as Jet Airways competed with foreign carriers to increase 
				India’s share of the market to 50 per cent. Flights to London, 
				New York, China, Toronto, Far East and the Gulf came about and 
				soon the airline’s reputation soared to new heights as a carrier 
				of choice, thanks to its excellent quality of service. <br>
				<br>
				At Jet Airways, Busaidy’s capabilities and abilities have been 
				stretched to the best and he has come up trumps assuming various 
				roles and responsibilities heading the strategic expansion 
				plans; fleet management, HR, investor relations and IT. The main 
				thrust was on the strategic side to manage the expansion plans 
				of India’s fastest growing private airline. Under his guidance, 
				Jet Airways expanded rapidly and today flies to 63 local and 
				international destinations.<br>
				<br>
				Working out of Mumbai, Busaidy got a taste of India, both on the 
				professional and personal fronts and is grateful to Jet Airways 
				for giving him this opportunity. At a personal level, Busaidy 
				was always fond of India but with Jet Airways he was able to get 
				intimate knowledge about India and its vibrant culture. And then 
				he got hooked to Bollywood movies and also had a chance to 
				travel all over India. He also loves Indian food. <br>
				<br>
				<b>India shining</b><br>
				India seems to have made a profound impact on Busaidy as his 
				statement reveals: “On the professional level, I lived in India 
				at a time when it was shining, (no doubt it is still shining). 
				India has a high growth rate second only to China and I could 
				witness that growth in terms of foreign investments, growth of 
				companies. And I saw the youth of India, which is highly 
				motivated and highly educated as well. And then you have the 
				middle class of 400 million whic is also a force to reckon 
				with.”<br>
				<br>
				According to Busaidy, it was interesting to deal with two 
				different generations of Indians, the old and young. He says 
				that he has been associated with India for almost 25 years and 
				could see the change, how the society has shifted to consumerism 
				and their aspirations know no bounds. Some of the top 
				billionaires are from India. It is quite fascinating.<br>
				<br>
				In terms of corporate culture, Busaidy says that working with 
				Jet Airways is completely different from Gulf Air and Oman Air. 
				When he was joining them, Jet Airways had just finished its IPO, 
				having raised a billion dollars in funds and it was an airline, 
				which was trying to find its place in the world arena. The 
				vision of the chairman is quite inspiring and his belief that 
				India can come up with a quality airline that the country can be 
				proud of and compete with the best in the world has been 
				achieved. <br>
				<br>
				In a private organisation, the decision-making is faster; one 
				has to succeed. There is no choice. “You have to really fight 
				and compete in order to achieve the high goals,” says Busaidy. A 
				father of four children, he proudly admits that he has also 
				become a grandfather recently. He always tries to have at least 
				two meals per day with his family whenever possible. Fishing, 
				reading, going to gym and de-stressing are some of the spare 
				time activities that he indulges in. <br>
				<br>
