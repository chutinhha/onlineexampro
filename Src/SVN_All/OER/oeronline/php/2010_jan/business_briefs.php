                    Petroleum Development Oman (PDO) has awarded an engineering, 
					construction and procurement (EPC) contract for the Saih 
					Nihayda gas compression project to South Korea’s GS 
					Engineering and Construction. The award also includes the 
					de-bottlenecking of the Saih Nihayda gas processing plant. 
					“This is an important project which aims to ensure that the 
					Saih Nihayda field produces gas at an optimum level for a 
					maximum amount of time,” commented Deputy Managing Director 
					Abdulla al Lamki. The Saih Nihayda field in Central Oman has 
					been producing gas since it was commissioned in early 2005. 
					Gas from the field is treated at the Saih Nihayda processing 
					plant from where it enters the government gas grid. Based on 
					current demand patterns, reservoir pressure is expected to 
					start declining within the next six years. The installation 
					of the new compression facilities will enable PDO to 
					maintain pressure for several years. The de-bottlenecking of 
					the processing plant will increase capacity from the current 
					level of 20 million cubic metres a day (m3/day) to 25million 
					m3/day.<br>
					<br>
					Front-end engineering and design (FEED) for the Saih Nihayda 
					compression and de-bottlenecking project was undertaken by 
					PDO’s FEED Office. This is the first PDO project designed 
					internally by the recently-created FEED Office.<br>
