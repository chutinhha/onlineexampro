                    </font><b><font face="Verdana" size="2">ESO launches 
				comprehensive website<br>
				</font></b><font face="Verdana" size="1"><br>
                    <b>An insight into the ongoing and future projects is being 
				offered on the website and will be useful to all those 
				interested in preserving the environment of Oman</b><br>
                    <br>
                    <img border="0" src="images/inthenews_img1_jan2010.jpg" width="200" height="130" align="left" hspace="9" vspace="5">A 
				comprehensive website of the Environmental Society of Oman (ESO),
				<a target="_blank" href="http://www.eso.org.om">www.eso.org.om</a>, 
				was launched by HE Sayyid Hamoud Bin Faisal Al Busaidi, Minister 
				of Environment and Climate Affairs. The website is not just for 
				environmentalists; it is useful for teachers, tourists, 
				government officials, parents as well as the media, officials of 
				ESO said. Katana Limited, a London-based creative studio, is the 
				designers behind the ESO website.<br>
				<br>
				The website highlights ESO’s major campaigns, such as its 
				plastic bag, recycling and anti litter campaigns, as well as its 
				conservation and research projects. The photographs have been 
				contributed by a wide variety of sources - from locals to 
				tourists to well known international photographers, who have put 
				their photos on shared sites such as ‘Flickr’. The result is a 
				collection of striking images representing the diversity and 
				richness of Oman’s environment.<br>
				<br>
				Lamees Abdullah Daar, executive director of the ESO, speaking on 
				the occasion said: “We have put in a lot of time and energy into 
				creating a website that covers all aspects of ESO’s work. The 
				website, both in Arabic and English is educational and 
				informative, and allows the users to learn more about our 
				projects and gain a good understanding of local environmental 
				issues.”<br>
				<br>
				“Another part of our website is devoted to young 
				environmentalists with fun educational facts and illustrations 
				created especially for ESO,” said Lamees. Environmental articles 
				written by members of ESO are downloadable and the site is 
				linked to Information Technology Authority (ITA) to allow for 
				online donations and payment of membership fees. Adam Thacker, 
				director of Katana Ltd, said: “Working on this project has been 
				a journey revealing the stunning beauty and diversity of Oman’s 
				environment. We look forward to continuing to support the ESO’s 
				work.”<br>
				<br>
				Since the Internet is by far the most convenient tool of 
				communications today, it is hoped that the ESO website will pave 
				the way for other organisations in Oman to use the Internet as a 
				tool for sharing information comprehensively, both in English 
				and in Arabic. Besides addressing the burning issues facing 
				environment, such as carbon footprints, global warning and 
				e-waste, it also keeps the doors wide open to other issues 
				confronted by the biodiversity, soil, plants, landscape, water 
				and climate change. A facility for joining the society is also 
				offered online. <br>
				<br>
				<b>Call to create knowledge society<img border="0" src="images/inthenews_img2_jan2010.jpg" width="200" height="122" align="right" hspace="9" vspace="5"><br>
				</b>The GCC eGovernment conference has created a platform for 
				authorities to explore ways of linking eGovernment portals of 
				member states.<br>
				<br>
				The first ever three-day GCC eGovernment conference was held at 
				Al Bustan Palace Hotel under the auspices of His Highness Sayyid 
				Asaad bin Tariq al Said. The eGovernment leaders and visiting 
				international Information Communication Technology (ICT) experts 
				urged an increase in the steps already under way to create a 
				Knowledge Society in the Gulf region.<br>
				The importance of this conference (held in December) stems from 
				the developments we are witnessing in the GCC to evolve and 
				achieve all the targets set in the implementation of eGovernment 
				plans. This was stated by Chairman of Oman’s Information 
				Technology Authority (ITA) HE Mohammed bin Nasser Al Khusaibi.
				<br>
				<br>
				In his keynote address, HE Khusaibi said the stage was set for 
				the GCC to rapidly build on the astonishing progress all six 
				nations have already made in eGovernance because science and 
				technology are the most important issues of concern to the world 
				in this age. Seah Chin Siong, the head of IDA International in 
				Singapore, in his speech, outlined the need for an integrated 
				government, which includes the shift from eGovernment to 
				iGovernment, establishing the underlying foundation to serve 
				citizens and customers better. <br>
				<br>
				<img border="0" src="images/inthenews_img3_jan2010.jpg" width="200" height="130" align="left" hspace="9" vspace="5">He 
				said Singapore has 1,600 e-services online so far and efforts 
				are underway to consolidate the country’s iGovernment 
				infrastructure. He focused on the successes and challenges that 
				the eGovernment face in order to improve services. Other key 
				speakers included Dr Salim bin Sultan al Ruzaiqi, CEO, ITA; 
				Mohammed Obaid al Mazroui, Assistant Secretary-General for 
				Economic Affairs at the GCC Secretariat-General; and Bader al 
				Dafa, Executive Secretary, UN Economic and Social Commission for 
				Western Asia.<br>
				<br>
				This conference has created a platform for GCC eGovernment 
				authorities to share their experiences, discuss the challenges 
				of implementing eGovernance, agree on the priority eServices to 
				provide in the GCC and explore ways of linking eGovernment 
				portals of member states. The decision for the Sultanate of Oman 
				to host the first ever GCC eGovernment Conference is a 
				recognition for the rapid growth of the eGovernment sector in 
				Oman and the country’s growing advancement in its delivery of 
				electronic services (eServices) to citizens and residents across 
				the country.<br>
				<br>
				<b>Exhibition</b><br>
				The 3-day event also featured an ongoing exhibition showcasing 
				the very best of eGovernment projects in the six GCC countries. 
				Three awards were awarded in five categories: e-Services; 
				e-Content; e-Maturity; e-Economy and e-Project, and culminated 
				in the awarding of the first highly anticipated eGovernment 
				Award, at the end of the conference.<br>
				<br>
				This award will become an annual event celebrating excellence in 
				the GCC in the effective delivery of eProjects and eServices.
				<br>
				<br>
				<b>Unlocking the complexities of pay market</b><br>
				The OER-Lighthouse Compensation Survey 2009 will enable 
				employers to make critical compensation related decisions backed 
				by the support of accurate research data<br>
				<br>
				The Lighthouse Compensation Survey 2009 is a follow up to the 
				extremely successful survey held in 2006, covering all major 
				corporate sectors in Oman. It is a multi-industry survey which 
				profiles over 1000 benchmark positions from over 100 
				organisations to generate comprehensive reports. By establishing 
				a benchmark for organisations to set up highly competitive and 
				effective compensation structure, strategy and systems, the 
				survey will give a more professional approach to compensation, 
				rewards and performance management in Oman. Also, along with the 
				ability to query, select and filter results by position, 
				geographic location and/or company size, the salary survey 
				results include a comprehensive set of descriptive statistics, 
				compensations analysis tools and salary trend.<br>
				<br>
				There are three types of reports available to customers – the 
				‘Standard Industry Report’ which gives you an overview of the 
				national economy, an overview of the chosen industry and 
				compensation and benefits trend of the chosen industry; the 
				‘Customised Industry Report’ which includes the features of the 
				standard report and also includes salary comparisons for all 
				positions listed along with detailed analysis of compensation 
				and benefits data along with variable pay levels present in the 
				chosen industry.<br>
				<br>
				Lastly the ‘Premium Industry Report’ which includes all the 
				features of the Standard and customised reports and additionally 
				provides an in depth analysis of the company’s position versus 
				the market, a detailed analysis of the cost impact to match the 
				market and detailed recommendations to address compensation 
				issues and a best practice report on reward strategy.<br>
