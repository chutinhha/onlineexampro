                    </font><font size="2" face="Verdana"><b>Wane of boom time<br>
				<br>
				</b>
				</font><font face="Verdana" size="1">
				<b>In this region, real estate demand has been linked to 
				resource prices and since the last few months have been a period 
				of stabilisation, buyers are slowly emerging</b><br>
				<br>
				Real estate as an asset class is prone to booms and busts, since 
				it takes time for supply to be built up and once it is built, it 
				takes time for over supply to be absorbed. The industry in any 
				country is highly fragmented since there are no economies of 
				scale in any part of the value chain be it design, purchase, 
				construction or leasing and selling. Thus there are no large 
				players who can better estimate demand and supply and smoothen 
				out the feast or famine nature of the industry. <br>
				<br>
				In normal times, in less developed economies, capital allocation 
				is not as efficient as in developed economies and hence the real 
				estate industry is more volatile in developing economies rather 
				than in the developed economies. In the US, which is one of the 
				most developed economies in the world, capital allocation 
				improved, with funding for real estate moving from smaller 
				players to Wall Street. The cyclical nature of the industry 
				remained but the volatility was much less for many years. <br>
				<br>
				Of course, in the last few years, Wall Street has completely 
				failed in its primary duty of allocating capital efficiently and 
				this had brought the entire financial system close to collapse 
				till central banks moved in and rescued it. Real estate was 
				particularly affected since it is one industry where risks are 
				perceived to be lower and hence the financial leverage is 
				usually much higher. Real estate mortgages in developed 
				economies are a very significant percentage of their GDP.<br>
				<br>
				It has been a feature of resource rich countries in Africa and 
				the Middle East that real estate prices closely track resource 
				prices. When resource prices move up, there tends to be a real 
				estate boom and when resource prices move down, real estate 
				prices tend to slump. A lot of lending to economic activity in 
				these countries tends to be against real estate as collateral, 
				and when real estate prices slump, banks shrink lending and this 
				amplifies the economic downturn brought on by the drop in 
				resource prices. <br>
				<br>
				The government of Oman has managed its finances very prudently 
				and thus one finds that in 2009, in spite of a steep drop in oil 
				prices the economy is likely to grow and not shrink. This is a 
				commendable achievement.<br>
				<br>
				Post the financial crisis, real estate prices in Oman slumped 
				significantly and what is more important is that the volume of 
				transactions dried up to a large extent as buyers stood still 
				expecting prices to drop further. The last few months have been 
				a period of stabilisation and buyers are slowly emerging after 
				concluding that prices will not drop any further. Volume of 
				transactions is up and we can expect 2010 to be a year when real 
				estate industry will perform well although the boom conditions 
				of 2008 will probably not return for many years to come. <br>
				<br>
				Asset bubbles take time to build and do a lot of damage to the 
				long term growth of the economy. Central banks all over the 
				world have seen the consequences of asset bubbles and will 
				probably do everything possible to avoid them in future. <br>
