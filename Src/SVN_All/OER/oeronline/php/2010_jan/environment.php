                    </font><font size="2" face="Verdana"><b>Countries to raise 
				funds in fight against climate change<br>
				<br>
				</b>
				</font><font face="Verdana" size="1">
				<b>There was a political agreement to cap temperature rise, 
				reduce emissions and raise finance at the Copenhagen United 
				Nations Climate Change Conference</b><br>
				<br>
				The United Nations Climate Change Conference in Copenhagen held 
				in December ended with an agreement by countries to cap the 
				global temperature rise by committing to significant emission 
				reductions, and to raise finance to kickstart action in the 
				developing world to deal with climate change.<br>
				<br>
				At the meeting, world leaders agreed the Copenhagen Accord, 
				which was supported by a majority of countries, including 
				amongst them the biggest and the richest, and the smallest and 
				most vulnerable.<br>
				<br>
				“We have sealed the deal,” said UN Secretary-General Ban Ki-moon. 
				This accord cannot be everything that everyone hoped for, but it 
				is an essential beginning, he said.<br>
				<br>
				The Copenhagen Accord recognises the scientific view that an 
				increase in global temperature below 2 degrees is required to 
				stave off the worst effects of climate change. In order to 
				achieve this goal, the accord specifies that industrialised 
				countries will commit to implement, individually or jointly, 
				quantified economy-wide emissions targets from 2020, to be 
				listed in the accord before January 31, 2010. A number of 
				developing countries, including major emerging economies, agreed 
				to communicate their efforts to limit greenhouse gas emissions 
				every two years, also listing their voluntary pledges before 
				January 31, 2010.<br>
				<br>
				Nationally appropriate mitigation actions seeking international 
				support are to be recorded in a registry along with relevant 
				technology, finance and capacity building support from 
				industrialised nations.<br>
				<br>
				Because the pledges listed by developed and developing countries 
				may, according to science, be found insufficient to keep the 
				global temperature rise below 2 degrees or less, leaders called 
				for a review of the accord, to be completed by 2015. The review 
				would include a consideration of the long-term goal to limit the 
				global average temperature rise to 1.5 degrees. Heads of state 
				and government also intend to unleash prompt action on 
				mitigation, adaptation, finance, technology, reducing emissions 
				from deforestation in developing countries and 
				capacity-building.<br>
				<br>
				To this effect, they intend to establish the “Copenhagen Green 
				Climate Fund” to support immediate action on climate change. The 
				collective commitment towards the fund by developed countries 
				over the next three years will approach $30bn. For long-term 
				finance, developed countries agreed to support a goal of jointly 
				mobilising $100bn a year by 2020 to address the needs of 
				developing countries.<br>
				<br>
				About 119 world leaders attended the meeting, the largest 
				gathering of heads of state and government in the history of the 
				UN. The next annual UN Climate Change Conference will take place 
				towards the end of 2010 in Mexico City, preceded by a major two 
				week negotiating session in Bonn, Germany, scheduled May 31 to 
				June 11. <br>
