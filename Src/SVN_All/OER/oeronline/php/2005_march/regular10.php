            <p align="justify"><font face="Verdana" size="4" color="#FF0000">New 
            ideas that stir your emotions</font><font face="Verdana" size="1"><br>
            <br>
            <b>Praveen V. Thampi provides an overview of today’s automotive 
            scenario</b><br>
            <br>
            <img border="0" src="im16.jpg" align="left" hspace="5" width="110" height="130">The 
            swiftly changing dynamics of the industry never stop amusing the 
            enthusiasts. As much as we think we have finally got what we want 
            out of a car, there is always more that the manufacturers excite us 
            with as we move on. Not to be overlooked are the vast changes taking 
            place in automobile manufacturing. Flexible factories are reducing 
            man-hours and costs per car while offering a much wider range of 
            choices for customisation. Today, more than ever, car manufacturers 
            and their suppliers are cooperating in the design and manufacture of 
            new cars in ways that are revolutionising the entire process.<br>
            <br>
            Moving out from the happenings of the past, we have come away with 
            some interesting things that changed the face of the automobile 
            industry. What makes some brands set themselves in the minds of the 
            people and get them to stick by them? There is one interesting 
            element that I have noticed in the different countries that I have 
            travelled to. If a particular brand manages to be at the top of the 
            mind of the people in one country, it is invariably some other brand 
            that is at the top in another. Interestingly, this indicates the 
            much varied tastes and beliefs of people from different regions or 
            countries. <br>
            <br>
            How much a manufacturer digs deep into the lives and imaginations of 
            people defines the extent to which they are able to connect with the 
            people of that region. Communication is the key word here, in my 
            opinion. Does that mean advertisement? Frankly, if we were to 
            evaluate the minds of people, we will no doubt see and comprehend 
            that advertising is a form of communication that from the point of 
            view of the recipient, is held in low esteem. <br>
            <br>
            You will see that for most people, advertising is unwanted, disliked 
            and even detested at times. Some intelligent soul once said that may 
            be it is this reputation of advertising that has made the field of 
            advertising a very good testing ground for theories of 
            communication. They say if it works well in advertising, it is very 
            likely that it will work well in politics, religion or any activity 
            that requires mass communication.<br>
            <br>
            I have always been keenly observing the various interesting 
            alternatives that automobile brands choose to be present and remain 
            in the minds of prospective buyers. Every brand has something 
            different to talk about and they talk about it with passion alright. 
            When you look at the automobile business from the outside, you see 
            that cars are just mobility solutions and some are rated excellent, 
            good and some a bit away from it. But people buy different cars for 
            different reasons and their perceptions differ. <br>
            <br>
            What one perceives about one brand of automobile in one country is 
            probably not the same as what someone in another country does for 
            the same brand. If all this sounds and feels complicated, it is 
            because it is. However, there is one simple common factor that 
            explains the reason why such varied feelings should exist within 
            different people for the different brands in different global 
            markets. We call it brand positioning. <br>
            <br>
            Of course, manufacturers stick to global brand positioning but the 
            emphasis given to their brands depends on the kind of market that it 
            is or even the kind of customer needs as they perceive it and is 
            able to cater to. No single car brand can be everything to everyone 
            and though some brands have tried their best to fall into this 
            category, in reality it would be the ones trying to cater to the 
            exclusivity of a prospective buyer that gains the lead. <br>
            <br>
            A thinking person would easily know that every car brand has 
            something that is unique to itself. Technology is not something that 
            a brand can entirely depend on for success, considering the 
            lightning speed with which technology transfers happen in this 
            enlightened world. What one brand does one day will quite naturally 
            be done by most others in a short span, which invariably becomes a 
            standard soon. This by far turns out to be a limitation and hence it 
            is far beyond just products and offerings that is enough to get into 
            that limited space that customers leave aside in their mind, for the 
            cars they want to own. <br>
            <br>
            Creating an identity that is exclusive and meaningful, generating a 
            sense of pride within prospective buyers by exciting them to be 
            associated with the brand and positioning their products in the 
            minds of this prospective customer turns out to be the priority for 
            most growth oriented brands. This is no easy game to play and most 
            players in the industry realise this fact. <br>
            <br>
            The centre point of this effort is not just limited to spending 
            major monies to communicate. The primary step towards this would 
            ideally be to define the brand DNA, keeping in view the strategic 
            direction that the brand aspires to take and then build on a 
            worthwhile plan to research, develop and produce products that match 
            this defined basic essence of the brand. <br>
            <br>
            Phenomenal amounts of monies and resources are being spent in the 
            development of a viable plan and then create dreams for the 
            aspirational buyer. Brand management has taken on a new significance 
            and urgency for the auto industry. Connecting your people with your 
            brand - the magic of practising what you preach.<br>
            <br>
            Brand positioning is a challenge for all automobile brands as much 
            as it is for any consumer/retail product. Everyone realises that 
            positioning is not about what we do to a product but pretty much 
            what we do to the mind of the prospective buyer. We live in an 
            over-communicated society and we all squeeze our way through the 
            various material we come across by means of the newspapers, 
            magazines, articles, Internet, hoardings, television, displays etc. 
            every single day. <br>
            <br>
            The per capita consumption of advertising and other communication is 
            at the highest levels that we have ever seen. The criminal amounts 
            of money spend on this activity by the majors seemingly creates only 
            a minor impact on the minds of the prospects considering the amount 
            of communication that he is expected to rake into his mind by the 
            different products and brands that we have. <br>
            <br>
            The volume of communication that we see these days clearly indicates 
            that a normal human brain cannot accommodate and also keep on the 
            top of their mind, all this information. Human mind has a defence 
            mechanism by which we screen and reject most of the unwanted (as 
            defined by our mind) information and only keep those that we have 
            either heard before or can relate well to. <br>
            <br>
            Exorbitant amounts of monies have been spend to challenge this and 
            try and change the mind set of people with advertising but most 
            people are made up of stuff that is harder to change since it is 
            easier accepting it that way. Automobile manufacturers have realised 
            over a period of time that the only hope to score big is to be 
            selective, to concentrate on the niche segment that you define as 
            yours and thus practice segmentation. This is positioning in the 
            real sense. <br>
            <br>
            The advertising strategy gurus of this world said and proved over 
            and over again that with the media explosion, product explosion and 
            the advertising explosion, it has become extremely important to be 
            focused on that prospect that fits well into your definition of the 
            right customer, for your product. <br>
            <br>
            For the automobile brands like any other, finding windows in the 
            minds of the prospects and identifying ways and means to place their 
            brand in his mind is the effort. A simplified message that is 
            sharpened enough to cut into the minds of the prospect well would 
            have a multifold impact on the prospects mind than a truckload of 
            information given without realizing the relevance. This simplicity 
            gives the brand a lasting impression in the mind of their prospect.
            <br>
            <br>
            Most brands that we recognise and talk about today in the automobile 
            industry went after the theory of owning a word, which they realised 
            soon, would do it all for them. If we were to observe the majors in 
            this world, we see that some words are owned by them and distinctly 
            represent their products by means of relating it to their prospect 
            and the prospects’ lifestyle. <br>
            <br>
            So much is happening within the industry and so much money goes into 
            just remaining there to be noticed and seen and liked by the 
            prospects. The manufacturers have a tough job on their hands trying 
            to meet with this ever-rising demand and still offer the dreams at 
            an affordable price. The cost pressure on automotive product 
            development is continuously increasing. <br>
            <br>
            A strategic review of automotive product development practices 
            indicates that the demand to develop more (vehicles) from less 
            (budget) will continue well into the next decade. Automotive 
            Engineering 2010, a research report prepared by Roland Berger 
            Strategy Consultants, predicts that suppliers will be responsible 
            for nearly 60 per cent of the industry’s research and development 
            work by the end of the decade, compared with approximately 40 per 
            cent today. <br>
            <br>
            Since the 1960s, the number of basic vehicle segments has grown from 
            four to more than 15. The industry’s top five manufacturers alone 
            are expected to introduce nearly 160 new models and facelifts across 
            the next 3 years. Besides, the number and complexity of new-model 
            features also continues to climb. It is amazing to note that while 
            the electronics content of an average car in the 1970s was less than 
            10 per cent, it is expected to top 40 per cent by the year 2010. 
            Additionally, regulatory requirements also will have a major impact 
            on product development. <br>
            <br>
            Rising fuel economy, safety and environmental standards will put 
            additional pressure on OEM product development. Despite significant 
            increases in product content and complexity, 
            research-and-development budgets have remained flat and are expected 
            to remain so in the future. Since 1998, R&amp;D budgets as a percent of 
            sales at five of the industry’s top OEMs have remained virtually 
            unchanged at an average of 4 per cent of sales. <br>
            <br>
            At the same time, leading manufacturers have reduced the amount of 
            time required to develop new vehicles from about 36 months in the 
            mid 1990s to 24 months or less today. While costs and complexity 
            have risen sharply in recent years, the price of an average vehicle 
            based on the US producer price index of motor vehicles has remained 
            virtually unchanged since 1993.<br>
            <br>
            An interesting report from Roland Berger observes a further 
            clustering of product, system and component development into global 
            centres of excellence. It is by far expected that Europe will 
            continue to lead in the development of small front-wheel-drive cars, 
            diesel technology and luxury vehicles. Small displacement engines, 
            multi-purpose vehicles, and hybrid platforms will be the led by 
            Japan. OEMs will look to North America for the development of light 
            trucks, SUVs, minivans, large segment vehicles and V-6 and V-8 
            engine families.<br>
            <br>
            All these activities to meet our aspirations keep the manufacturers 
            motivated to continue developing products that touch our mind and 
            heart, besides actually meeting our needs. <br>
