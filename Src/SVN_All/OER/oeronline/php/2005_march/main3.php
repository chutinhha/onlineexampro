<p align="justify"><font face="Verdana" size="4" color="#FF0000">The leader as 
negotiator<br>
</font><b><font face="Verdana" size="1">By Phil Lowe, MCE Faculty</font></b><font face="Verdana" size="4" color="#FF0000"><br>
<br>
</font><font face="Verdana" size="1">
<img border="0" src="im6.jpg" align="left" hspace="5" width="197" height="132">An 
old friend of mine left his job as a sales executive with a global corporation 
to become headteacher of a school for children with educational and behavioural 
difficulties. He deals every day with significant leadership challenges: staff 
motivation; performance management; communicating with conflicting stakeholder 
demands. He recently told me that the best training for his current role was the 
negotiation training he had received from his previous employer. <br>
<br>
It’s very easy to take a narrow view of the skills of effective negotiation as 
something which only applies to meetings between customer and supplier. Even if 
we take a wider perspective, it’s tempting to think of negotiation as being just 
about money. In fact, one of the central principles of negotiating effectively 
is that true negotiation can never be just about money. True negotiation is 
about creating long-term shared value between two or more parties and, as such, 
it is more than a core sales or purchasing skill: it is a core leadership skill.
<br>
<br>
Take the area of performance management as an example. Let’s say I would like 
one of my team members to agree to a set of performance goals for the year. She 
disagrees; she sees the goals as imposed, and she has a number of grievances 
which lead her to mistrust the motives of senior management in setting these 
particular goals. So far, so familiar. The default choice in such a situation is 
for me as the leader to either demand concessions (“I’m the boss and what I say 
goes – I’m not interested in your objections”) or to make concessions (“I don’t 
want my team members to be upset. Don’t worry about the goals, I’ll take care of 
them”). <br>
<br>
The first response, known to negotiators as ‘win-lose,’ focuses on dealing with 
the issue at the expense of the relationship. The second option, ‘lose-win’, 
focuses on preserving the relationship but allows the issue to remain 
unresolved. Both options solve the short-term problem, but will destroy value in 
the long term: the first option allows a potentially productive long-term 
relationship to deteriorate; the second takes a ‘soft’ approach to performance 
goals which may have a negative effect on the business. <br>
<br>
The ‘principled negotiation’ approach aims to take a long-term or ‘win-win’ 
approach by focusing simultaneously on the issue and the relationship. Clearly, 
there will have to be concessions in the situation outlined above for it to be 
resolved; but rather than making them or demanding them, we encourage 
participants to always trade concessions. In other words, for every concession 
you make to improve the situation for the other party, get them to agree to give 
you something that is valuable to you. The wording of such an offer would be “If 
you… (agree to give me some of what I want), then I…. (might be prepared to give 
you something of what you want).” <br>
<br>
Such an approach relies on the negotiator using that most difficult of all 
skills: listening. In our example, my first task would be to invite my team 
member to explain in detail the needs and interests which lie behind the 
position she has taken (“I won’t accept these goals”). Her objection may not be 
to the goals themselves, but to some aspect of the work situation which it is 
relatively easy for me to resolve. <br>
<br>
I might then be able to say “If you were to agree to commit to this particular 
goal, I would be prepared to do something about (say) flexible working hours.” 
With some flexibility, both parties may get quite close to everything they want 
(which is why we say negotiation can never be just about money: if there is €100 
on the table, you and I can split the ‘pie’ a number of ways, but it will never 
add up to more than €100; but where other things can be put on the table, the 
original pie can be made bigger).<br>
<br>
Goal-setting discussions are not the only leadership area in which these skills 
can be applied. Others include: <br>
• Getting someone to agree to change their behaviour <br>
• Negotiating someone’s time to commit to an internal project <br>
• Balancing conflicting demands within a matrix organisation <br>
• Implementing organisational change <br>
<br>
All of these situations are tricky for the same reason as ‘traditional’ 
negotiations are tricky: you find yourself dealing with other human beings who 
may have completely different priorities – and personalities – than you. Two 
core principles can help you prepare: <br>
<br>
1. Separate the people from the problem: Negotiations fail when we allow 
emotions to colour our judgement, by criticising the other party, becoming 
defensive or misreading body language (that apparent expression of disapproval 
may just mean the other person has a bad headache!). In all situations, 
effective leaders, like effective negotiators, remain ‘hard on the issue, soft 
on the people’. You can still achieve a win-win with someone you hate – it’s 
just a question of finding where your mutual interest lies. Which brings us to:
<br>
<br>
2. Focus on interests, not positions: In any of the above situations, you will 
find the other party making ‘black and white’ statements (“I’m not prepared to…” 
“I can’t possibly agree to….” “That’s the best I can do”). If you get hung up on 
the positions the other party takes, you cannot reach a win-win solution. 
Instead, use listening and questioning to find out why they feel so strongly 
about a particular issue, and see if you might be able to offer something to 
meet the needs and interests which lie behind the positions they take. <br>
This is especially useful in any change situation: people don’t resist change 
because they’re bad people; they resist it because they don’t see their personal 
needs being met. The best change leaders enter into a negotiation with those 
affected to see if they can construct a solution which meets the needs of all 
parties.</font></p>

