<b>Destination Oman</b><br>
Oman’s travel and tourism industry seems dynamic, witnessing a lot of regional 
traffic. In 2002, Oman attracted some 1.2 million foreign visitors, of which 
700,000 came from the GCC states. A survey by the organisers of the Arabian 
Hotel Investment Conference (AHIC) puts Oman, Qatar and UAE (outside Dubai) as 
the top three emerging tourism markets in the region. According to WTTC, the 
travel and tourism demand in Oman is expected to achieve a real annualised 
growth rate of 5.5 per cent over the next 10 years to reach RO2.4 billion ($6.2 
billion) in the year 2014.<br>
<br>
Besides, the sector can provide employment to many in the country. As forecasted 
by the WTTC, nearly 114,823 jobs—11.8 per cent of total employment—will be 
generated by 2014. Initially, only business travellers and tourists on expensive 
package tours were allowed in Oman. Budget travellers were not favoured much. 
The current limit on tourists is set at 50,000 a year, but now the government 
plans to expand that number to 100,000 a year in 10 years, and 728,000 a year by 
2020. <br>
<br>
Going by the data released by WTTC, Oman’s personal travel and tourism 
industry—which is roughly around RO398.5 million ($1,036.5 million) or 10.7 per 
cent of total personal consumption—should reach RO656 million ($1,706.2 million) 
or 12 per cent of total personal consumption by 2014. <br>
<br>
Besides, business travel should also increase to RO219.5 million ($570.8 
million) by 2014. Realising the immense potential this sector has to offer, the 
government is investing heavily in it and hopes to increase the contribution of 
tourism industry to the national GDP to 3 per cent by 2020, up from the current 
0.3 per cent. This 10-fold increase will raise tourism revenues to RO130 million 
($338 million).<br>
<br>
The recently held conference on Built Environments for Sustainable Tourism 
(BEST) in Muscat highlighted the tourism-promotion initiatives that are on the 
anvil and ways to improve the tourism sector in the country. The WTO applauded 
the Omani government on its initiative in convening this first-ever 
international forum on BEST.<br>
<br>
A Muscat Declaration was adopted at the end of the conference, which calls for 
the promotion and establishment of partnerships among tourism stakeholders, 
public and private, in order to ensure an orderly and sustainable development of 
tourism infrastructures that respect local cultures and economies. The charter 
also seeks to promote the use of quality and adapted modern architecture and 
infrastructure in the development of tourism facilities in a way respectful of 
the built or natural environment, reinforcing positive destination images. It 
urges authorities and the industry to monitor the impact of tourism on the built 
environment from the standpoints of economic, cultural and environmental 
sustainability. <br>
<br>
The Sultanate is also investing in billion-dollar tourism projects to provide 
world-class facilities to tourists in the Oman. The Chedi Muscat is one such 
example. However, the most talked about project is The Wave, Oman’s most 
ambitious tourism development project encompassing integrated residential and 
tourism resort along the Muscat coastline. Another very important project is the 
Shangri-La’s Barr Al Jissah Resort and Spa, which is scheduled to open in 
October 2005. Set in 124 acres of landscaped garden, the resort is located on 
Oman’s magnificent coastline.<br>
<br>
Apart from the huge investments being made to improve the infrastructure, the 
country’s airline sector has also contributed to the tourism industry. Gulf Air 
has introduced daily flights between London and Muscat whereas Swiss 
International Airlines (SWISS) introduced daily flights between Muscat and 
Zurich late last year. Oman Air, the national carrier, has also increased the 
number of flights between Dubai and Muscat to 54 flights a week from 47. <br>
<br>
The government is also focusing on its marketing strategy to promote Oman as a 
tourist’s haven. Recently, the Sultanate’s newly formed Tourism Ministry 
appointed the Dubai-based Landor Associates to boost the country’s image. The 
government has allocated $30 million for internationally marketing the country 
through 2005. Various festivals are also being organised in the country to 
promote tourism, the prominent ones being the month-long Muscat Festival 
(January-February) and the Khareef Salalah Festival (July-August). <br>
<br>
Despite the numerous efforts being made, there are still many loopholes that 
hinder the development of the industry. For example, the southern region, which 
provides the country’s best natural gifts for attracting tourists, suffers from 
poor infrastructure and little exposure.<br>
<br>
However, the hope is that the newly formed Tourism Ministry is well focused on 
developing tourism in the country. The Ministry is working hard and, as HE 
Mohsin bin al Balushi, Undersecretary of Tourism, says: “The Ministry will 
create a surge in the development of the tourism sector by stimulating the 
promotional and marketing activities in this sector and attracting local and 
foreign investments into tourism projects. This would fulfil the wishes of His 
Majesty the Sultan for giving the tourism sector all possible importance in the 
future plans, diversifying the tourist potentials of different regions of the 
country, promoting domestic tourism, developing cultural tourism and showing 
attention to environmental tourism to make Oman a favourite destination of 
tourists.’’ </font></p>

