<p align="justify"><font face="Verdana" size="4" color="#FF0000">Tourism Trends<br>
</font><br>
<font face="Verdana" size="1"><b>After three subdued years with an accumulated 
growth of less than 1 per cent, international tourism bounced back in 2004. 
International tourist arrivals worldwide reached an all-time record of 760 
million – an increase of 10 per cent over 2003. The good news is that the 
amazing results are expected to continue in the coming years. Even though all 
regions of the world are forecasted to grow in terms of inbound tourist 
arrivals, the competition will be intense. In such a scenario, how does the 
Middle East and, in particular, Oman, stack? Can the region beat the more 
lucrative Asia-Pacific destinations and can Oman grab a bigger chunk from its 
rival neighbours? Shuchita Kapur reports</b><br>
<br>
The year 2003 was one of the worst years in the history of tourism. The industry 
was badly hit by the Iraq war. After the war, markets were just beginning to 
show signs of a recovery again when they were hit by the outbreak of SARS – the 
impact of which on travel demand was greater than any other event in history. 
According to data released by the World Travel Organisation, the volume of 
international tourism (measured in terms of international tourist arrivals) slid 
by 1.7 per cent to 691 million, as against a drop of 12 million arrivals on the 
2002 volume of 703 million. <br>
<br>
Going by region, a drop of 12 million arrivals (–9 per cent) was recorded in 
Asia Pacific due to the SARS scare. The Americas also recorded a decrease (–3 
per cent) while Europe barely consolidated its 2002 figure (+0.4 per cent). 
However, the Middle East and Africa recovered. They showed the best results 
among all the regions by posting a 3 per cent (approximate) increase. Although 
conditions improved in the latter half of 2003, overall, the industry could not 
make much of a recovery. <br>
<br>
Fortunately, the downward trend did not continue in 2004 and, as per forecasts 
and trends, the travel industry made considerable growth, the highest since 
1976. World Travel Organisation (WTO) Secretary-General Francesco Frangialli, 
said, ‘‘Tourism in 2004 reached a record of 760 million international tourist 
arrivals and the best growth rate of the last 20 years. <br>
<br>
‘‘We are confident that the tourism sector is back on the right track after 
three difficult years and although still in commotion over the tragic events in 
Asia, the tourism sector will surely show again its extraordinary resilience and 
its ability to overcome difficulties by making an important contribution to the 
quick recovery of the affected countries.’’ <br>
<br>
In 2004, while results were positive for all regions, not surprisingly, 
countries worst hit in 2003 registered the most impressive growth. Asia Pacific, 
hardest hit by the SARS outbreak, led the growth trend. It posted an increase of 
29 per cent, receiving around 154 million tourists. The Middle East stood second 
with a 20 per cent growth. It overcame the disruption caused by the Iraq 
conflict and attracted nearly 35 million visitors. <br>
<br>
Americas followed the leaders with a 10 per cent advancement after more than 
three years of losses. Even though it could not beat its 2000 record of 128 
million arrivals, it got a decent share of nearly 124 million international 
tourists. The North African destinations of Morocco and Tunisia drove growth in 
Africa and pushed the growth rate of the region to an estimated 7 per cent. 
Africa witnessed an estimated 33 million arrivals in 2004. Tourism growth in 
Europe did not change much – it grew at a rate of 4 per cent, with a total of 
414 million tourists. <br>
<br>
<b>Forecast 2005</b><br>
Expectations for 2005 are moderate, say analysts. The big challenge for the 
industry is to sustain last year’s results and improve growth. According to the 
WTO, leisure tourism is expected to sustain its growth while business tourism 
will recover further. This seems plausible, as economic conditions are good. 
Growth is anticipated for both established and emerging source markets. 
Inflation will remain under control and, as expected, interest rates should not 
move to the higher side. <br>
<br>
Bad news is that oil prices should not escalate further, even though it did not 
make a dent in the global economy last year. ‘‘It appears that tourism business 
is very much back to normal and we expect to see that trend reinforced in the 
year to come. An interesting finding from the last survey of the WTO Panel of 
Tourism Experts is that a clear shift could be observed from a focus on external 
factors and crisis management back to a renewed focus on the internal 
competitive factors of destinations and products such as product development, 
training and promotion,’’ affirms WTO Deputy Secretary General Dr. Dawid de 
Villiers.<br>
<br>
As of now, international tourism should show a worldwide growth pattern that 
will meet the WTO long-term forecast of 4 per cent a year.<br>
<br>
<b>Big bucks</b><br>
Undoubtedly, the tourism industry will play a vital role in shaping many 
economies. In the future, there will be many countries like Switzerland which 
rely so heavily on tourism. According to the WTO, tourism (international tourism 
and international fare receipts) represents approximately 7 per cent of 
worldwide exports of goods and services (as expressed in $), occupying the 
fourth position in the ranking after exports of chemicals, automotive products 
and fuels. <br>
<br>
Tourism demand above all depends strongly on the economic conditions in major 
generating markets. As said by Francesco Frangialli, tourism is bound to make a 
major impact on the international economy. <br>
<br>
According to WTO estimates, tourist inflow will increase tremendously in the 
years to come. The following chart shows the future trends of the industry.<br>
<br>
<b>Middle East</b><br>
Tourism was progressing well in the Middle East until 2001. Apart from during 
the 1980s, the Middle East has witnessed faster growth in international tourist 
arrivals when compared to other regions. The region’s share of international 
tourist arrival jumped from 1.6 per cent in 1975 to 2.4 per cent in 1998. Total 
arrivals in the Middle East reached 15.3 million in 1998, about four times the 
level of 1975.<br>
<br>
According to the WTO, current trends show that tourism continues to be one of 
the most dynamic economic sectors in the Middle East. Today, it is of vital 
importance to the region, mainly due to the depleting oil reserves. By 
developing tourism, the region is trying to move away from an oil-centric 
economy. In the long run, this is extremely important to sustain development. In 
terms of travel and tourism’s contribution to economy and GDP, the Middle East 
reached $72 billion (RO27.70 billion) in 2004, contributing 9 per cent to total 
GDP and experiencing 6.3 per cent real growth. <br>
<br>
According to the World Travel and Tourism Council (WTTC), the sector represented 
nearly 3.423 million jobs in 2004, representing 8.2 per cent of total 
employment, with 169,700 new jobs created in 2004 throughout the Middle East.
<br>
<br>
<b>Registering recovery</b><br>
Most countries in the region are embracing tourism in a big way. Consequently, 
seven of WTO’s list of top 30 emerging destinations in the period 1995 to 2000 
are in the Middle East: Oman, Saudi Arabia, Egypt, Syrian Arab Republic, 
Bahrain, United Arab Emirates, and Lebanon. <br>
<br>
Tourism in the Middle East grew by an average 10 per cent in the late 1990s. The 
growth showed improvement at the turn of the century – in fact, in the year 
2000, it grew by 13 per cent. However, during the first 8 months of 2001, due to 
the increased tensions in the region, growth slowed dramatically to 0.4 per 
cent. The tragic events of September 11 further hit the industry badly and 
caused a 9 per cent decrease in the last four months of 2001. This resulted in a 
2.5 per cent decline compared with the world tourism decline of 0.6 per cent.
<br>
<br>
The global impact was felt by the entire region. Egypt being one of the hottest 
tourist destinations in the Middle East suffered after the September 11, and 
tourism, which from 1995 to 2000 grew at an average rate of 12 per cent per 
annum, slumped 14.8 per cent in 2001. The country was hit by a spate of 
cancellations following the tragic events of 9/11, and hotel occupancy dropped 
to 54 per cent and air traffic declined by 50 per cent. The fallout from the 
September 11 attacks lead to a considerable loss in tourism revenues, second 
only to the loss suffered due to the decline in the Suez Canal traffic.<br>
<br>
Coming to 2003, Middle East did not perform all that badly even when the tourism 
industry was in doldrums. Development of intra-regional traffic and the 
continuous investment in the sector helped the region in registering a growth of 
over 3 per cent in international tourist arrivals and about 16 per cent in 
tourism receipts. Main destinations such as Egypt (+17 per cent), the United 
Arab Emirates (+8 per cent) and Lebanon (+6 per cent) showed good results. 
Bahrain (-7 per cent), Jordan (-3 per cent), Syria (-3 per cent) and Saudi 
Arabia (-2 per cent), however, saw arrivals decrease.<br>
&nbsp;<br>
After having gone through the not so good 2003, the region surpassed all its 
past records in 2004. During this period, the Middle East became the fourth most 
visited region in the world, with 35 million tourists visiting the region. Not 
only did it record huge volumes, but also beat Africa for the first time in 
2004. Nearly all the countries in the region showed improvement and, as the 
trends and forecasts show, all countries will at least sustain and some will 
show better results this year. <br>
<br>
Huge investments to improve the infrastructure will go a long way in promoting 
tourism. The region’s airline sector is also becoming an important means for the 
region both to achieve greater awareness in tourists generating marketing, and 
in providing the requisite increased capacity to bring in the tourist volumes 
forecast for the region. Moreover, most Gulf countries are embarking on an 
extensive marketing strategy.<br>
<br>
According to the WTTC’s Tourism Satellite Account Research highlights, the most 
promising countries include Lebanon, Qatar and the United Arab Emirates. These 
countries have enough potential to sustain and improve the tourism sector. 
Lebanon, a promising country has attracted major investments particularly from 
the GCC countries. Considered as the summer playground for Arab vacationers, the 
capital Beirut has also been named by an international retail organisation as 
one of the leading shopping destinations in the region.<br>
<br>
Kuwait, despite being an oil-based economy, is opening its doors to tourism. 
Having faced some problems, the country has shown a good performance in its 
travel and tourism activities. Investments bore results in 2004, and its visitor 
exports continued over normal rate of growth yielding higher GDP and employment.
<br>
<br>
Qatar, one of the fastest developing countries in the GCC, is leaving no stone 
unturned to be on the forefront and to become the Gulf’s most preferred 
destination for upmarket travellers. Jordan, which relies heavily on tourism for 
foreign exchange, has inked a favourable deal between the country’s private and 
public sector. As a resultant, it posted 18 per cent growth in the number of 
arrivals in the first eight months of 2004, according to WTO figures. <br>
<br>
Similarly, Syria has registered a jump in the number of tourist arrivals. In the 
first 10 months of 2004, inbound arrivals to the country from the GCC rose 
approximately by 18 per cent as against the same period of 2003. Bahrain also 
saw a 19 per cent increase in the first eight months of 2004 and Dubai reported 
a 9 per cent growth in visitor arrivals compared to the same period last year.<br>
<br>
Forecasts for international tourist arrivals to the Middle East show that there 
will be 68.5 million arrivals in 2020. This represents an annual growth rate of 
7.1 per cent over the period 1995-2020, which is above the global growth rate of 
4.1 per cent. Consequently, the overall share of arrivals to the Middle East 
region is expected to increase over the forecast period. <br>
<br>
The Tourism 2020 Vision study by the WTO forecasts that there will be over 35 
million outbound tourists arrivals generated from the Middle East in the year 
2020. The individual inbound country forecasts for the main tourist destination 
within the Middle East show that Egypt will remain the region’s largest tourist 
receiving country. By 2020, it is expected that there will be over 17 million 
tourist arrivals to the country.<br>
<br>
As per the WTO estimates, total tourist arrivals by region shows that, by 2020, 
the top three receiving regions will be Europe (717 million tourists), East Asia 
and the Pacific (397 million) and the Americas (282 million), followed by 
Africa, the Middle East and South Asia.<br>
<br>
East Asia and the Pacific, South Asia, the Middle East and Africa are forecasted 
to record growth at rates of over 5 per cent per year compared to the world 
average of 4.1 per cent. The more mature regions, Europe and Americas are 
anticipated to show lower than average growth rates. Europe will maintain the 
highest share of world arrivals, although there will be a decline from 60 per 
cent in 1995 to 46 per cent in 2020. As per the WTO’s estimates, the Middle East 
will remain one of the world’s smallest tourists generating regions – however, 
it is gaining worldwide market share. <br>
