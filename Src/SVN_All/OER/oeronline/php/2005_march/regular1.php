<p align="justify"><font face="Verdana" size="4" color="#FF0000">Banking on 
innovation</font><font face="Verdana" size="1"><br>
<br>
<b>Ali Hassan Moosa outlines how and why banks in the Sultanate need to 
continuously innovate their products as well as delivery channels in order to 
keep pace with discerning consumers’ wants and desires</b><br>
<br>
<img border="0" src="im8.jpg" align="left" hspace="5" width="110" height="132">Innovation 
is widely proclaimed as being of vital importance to achieve and maintain 
competitive advantage. Furthermore, sustaining competitive advantage depends 
heavily on the ability of the organisation to internalise the benefits of 
innovative activities.<br>
<br>
Broadly speaking, innovation can be defined as the progression that brings about 
technological, process or product change in organisations for the benefit of 
stakeholders. Innovation needs to result in enhancing shareholder value, greater 
efficiency and improving the quality of service.<br>
<br>
Historically, financial services were not known for innovation. Checking account 
products, for example, are similar from one bank to the next, with only slight 
variations between them. Yet, competition is so intense in today’s retail 
marketplace that institutions must seek every advantage to gain market share. 
While innovative products can often provide that edge, the trick is to produce a 
constant stream of innovations so that gains are consistent and repeatable.<br>
<br>
Product innovation has a market focus and is primarily customer driven. It can 
be defined as the introduction of new products or services to meet customer 
expectations and to increase market share. On the other hand, process innovation 
is internally focused and is efficiency-driven. It can be defined as seeking new 
capabilities or competencies to enhance existing services on offer.<br>
<br>
Innovation in banking needs to be a client-driven function since customers are 
entitled to the widest range of products in the most convenient and efficient 
manner. Focusing on clients leads to thinking outside the box – it leads to 
change. Any financial institution that wishes to succeed must recognise a simple 
fact – the customer is in charge.<br>
<br>
One of the greatest barriers to innovation is mistrust between bankers and 
clients on the one hand and between bankers and bankers including colleagues 
working within the same institution on the other. Cooperation and collaboration 
is key to improving products and services. <br>
<br>
Other key issues of hindering successful adoption of technologies and new ideas 
include resistance to change, cultural inertia, internal polices, fear of 
cannibalising existing products, fear of destroying existing competencies, 
satisfaction of the status quo, and in general, lack of enthusiasm to abandon 
the current way of doing things.<br>
<br>
The complexion of the banking sector has changed dramatically in terms of 
products and services in a market where the customer has more options than ever 
before and banks are compelled to review constantly their package of products 
and services. <br>
<br>
Banks can no longer afford to only product innovate as fierce competition has 
lead to new ways of thinking on how various products are packaged together and 
personalised for clients. New products, no matter how brilliant they may be, 
have little chance of success unless they are sold to customers as part of a 
dazzling packaged deal.<br>
<br>
Marketing has become crucial for a bank’s success – in terms of profitability, 
innovative product development, optimum use of infrastructure, expanding market, 
and so on. Customers today are looking for convenience – where and how to offer 
the product no longer matters. This is why branding is gaining importance. <br>
<br>
Traditionally, banking brands have been particularly difficult to build. This is 
largely because the financial products offered by most banks are not exciting or 
do not usually enhance the bank’s image. In fact, with fierce competition, there 
is little or no sustainable product innovation/differentiation – either in 
product features or the price. Activity-based pricing with the aid of scientific 
methods based on customer preferences is essential. Most customers do not mind 
paying a little extra if they get the service they want. <br>
<br>
The banking industry has witnessed platform automation across branches, call 
centres with wider responsibilities, and Internet banking. The challenge for 
banks is to devise different distribution strategies for different market 
segments.<br>
<br>
Over the last few years, the Internet has transformed the way in which banks in 
developed economies provide trade services to corporate clients, by enabling 
direct data entry and providing corporate users up-to-date information on their 
account transactions. <br>
<br>
Locally, some banks have developed software packages for corporate users, but 
unfortunately, the demand for such products from clients has been relatively 
poor. This could perhaps be due to the security risks associated with electronic 
data transmission, especially over the Internet. Remote services offered by 
banks do conform to industry standard security protocols that incorporate high 
level of encryption and digital authentication facilities. <br>
<br>
Financial regulators play an important role in fostering financial innovation. 
Effective financial regulation assures safety and stability of financial 
institutions for the protection of shareholders and the public. It preserves 
fair competition in the interest of public, and last but not least, it 
encourages innovation.<br>
<br>
Locally, the banking regulator, the Central Bank of Oman (CBO), has an excellent 
track record of not only encouraging innovation among banks, but also leading 
the process of modernisation. Examples include projects such as Banking Credit 
Statistics Bureau (BCSB), Automated Teller Machine (ATM) national switch, and 
the current on-going development of a Real Time Gross Settlement System (RTGS).<br>
<br>
Let us look briefly across the border to see the recent developments in the 
industry. The UAE’s banking industry is regarded as highly innovative because of 
the fierce competition among banks – there are 48 banks in a country of 2 
million consumers. <br>
<br>
Competition normally creates pricing pressure, but also forces banks to be more 
creative in differentiating their products and services, and leads to brand 
creation, service excellence, and the application of advanced risk management 
methodologies. The sheer existence of foreign banks influences local banks to 
adopt world-class operational and marketing processes.<br>
<br>
Dubai Bank is a relatively new entrant in the banking industry, but since its 
launch towards the end of 2002, it has challenged traditional banking practices 
by placing customer service at the forefront of its operations. Through an 
inventive approach to banking, Dubai Bank has introduced a wide array of 
un-witnessed products and services that include car loans with extended dealer 
warranty, personal loans with added benefits such as free life insurance, and 
money delivery service.<br>
<br>
Emirates Bank’s retail arm—meBANK—is a contemporary and trendy addition to the 
banking industry in the UAE. Their products and services are designed to meet 
the lifestyles and personalities of customers. Union National Bank’s (UNB) 
latest product is car equity loan where a customer may use his/her car as a 
collateral to obtain credit facilities from the bank – a similar concept to home 
equity loan.<br>
<br>
In the UK, acclaimed entrepreneur Richard Branson’s Virgin Financial Services 
has introduced a new type of mortgage account that helps customers pay their 
mortgages earlier, thus saving them money. The product consolidates customer’s 
asset and liability accounts based on agreed set intervals and the result is 
that any savings that the customer may have would automatically be adjusted 
towards the mortgage account in order to reduce the outstanding liability and 
cost.<br>
<br>
There is no doubt that the most forceful change in the banking industry today is 
the rapid evolution of consumer wants and desires. Consumers are demanding 
anytime anywhere delivery of financial services along with an increased variety 
in loan, deposit, and investment products.<br>
<br>
Banks must continue to innovate in order to meet the changing needs of 
consumers, while at the same time developing strategies (through a fee 
structure) to migrate customers from high-cost delivery channels to alternate 
delivery channels. This blend of innovation and consumer behaviour change lies 
at the heart of modern banking organisations.<br>
