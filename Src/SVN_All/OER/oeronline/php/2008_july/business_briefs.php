<font size="2" color="#CC3300" face="Verdana"><b>Five companies get Class II TRA 
licence </b></font><font face="Verdana" size="1"><br>
<img border="0" src="business_briefs6.jpg" align="right" vspace="5" hspace="5" width="236" height="158">The mobile telephone market in the Sultanate of Oman is set for a sea change 
with the Telecommunication Regulatory Authority (TRA) issuing Class II licences 
to five companies as resellers of basic mobile services in the Sultanate.<br>
These licences have been granted to Injaz International, Kalam 
Telecommunications, Majan Telecom, Mazoon Mobile and Arab Link. According to TRA, 
the licensees can resell basic mobile services after buying airtime from 
operators at bulk rates in accordance with commercial agreements concluded with 
the operators. These services can then be rebranded and offered to subscribers 
at retail rates. Meanwhile, Oman is also gearing up to be the first regional 
country to get a Mobile Virtual Network Operator (MVNO). The MVNO operational 
model, while new to the Middle East region, has been proven to deliver 
significant benefits to consumers, telecom operator partners and the general 
economic welfare of countries around the world.<br>
