<font size="2" color="#CC3300" face="Verdana"><b>DRIVEN brings Le Mans to Oman</b></font><font face="Verdana" size="1"><br>
<img border="0" src="business_briefs4.jpg" align="right" vspace="5" hspace="5" width="248" height="153">The spirit of Le Mans was seen for the first time in Oman when DRIVEN, the 
newest and most popular motor magazine, and Peugeot, joined hands to set a 
record of travelling across Oman in under 24hours. The MCT-SAL-MCT round trip 
was covered in 20hours and 15minutes while staying well under the legal 
speed-limit with an average speed of 100.74kmph. The record, christened LeOman24 
by DRIVEN, was to drive just as it is done in the Le Mans. Originally called 24 
Heures du Mans (or 24 Hours of Le Mans), it was organised to not only test a car 
and driver’s ability to be quick, but also to last over a 24-hour period. The 
LeOman24 Team, which included both the drivers and organisers, decided to take 
further inspiration from Le Mans by following certain rules of the 24-hour race. 
For example, until the 1980s a minimum of two drivers were required to compete, 
but by the end of the decade at least three drivers were necessary. The LeOman24 
drivers were also three; Moadh al Zadjali (OAA), and Malcolm Xavier and Rathish 
Ramachandran (DRIVEN).<br>
Another rule is a requirement for cars to be shut off while they are being 
refueled in the pits. We did the same with similar interests to push the 
reliability envelope further. Says Rathish Ramachandran, editor, DRIVEN, “Safety 
was of course an important factor in this event: All drivers possessed much 
experience in night driving under stressed conditions, while special care was 
also taken such as being belted and not carrying extra on-board fuel. The 
vehicle was given a service and a thorough check before being passed as 
trip-worthy.” Moadh al Zadjali, of OAA said, “Speed limits and driving rules and 
ethics were followed and monitored. We are pleased to say that this event has 
much potential and hope to do it on a larger scale next year.”</font><hr width="90%" size="1">
