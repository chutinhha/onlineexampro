</font><font size="2" color="#CC3300" face="Verdana"><b>US SME DEVELOPMENT TEAM 
SHARES BEST PRACTICES WITH THE MABC </b></font><font face="Verdana" size="1">
<br>
<img border="0" src="business_briefs10.jpg" align="right" vspace="5" hspace="5" width="300" height="200">On June 11, an expert team from the consulting firm Booz Allen Hamilton 
discussed the potential of Small and Medium-Sized Enterprise (SME) growth in 
Oman during a breakfast discussion at the US Embassy with members of the Muscat 
American Business Council (MABC). The team was in Muscat to assist the Ministry 
of Commerce and Industry’s Directorate General of Small and Medium-Sized 
Enterprises (SMEs) in developing strategies for promoting this promising sector 
in light of the US-Oman Free Trade Agreement (FTA).<br>
<br>
Sponsored by the US Department of State’s Middle East Partnership Initiative (MEPI), 
the team is advising Ministry officials on SME international best practices to 
foster the growth of this important component of Oman’s economy. They are 
working with the ministry to develop organisational structures to better deliver 
SME services to clients, with special emphasis placed on women entrepreneurship. 
Booz Allen Hamilton is joined by the International Executive Service Corps (IESC), 
which is charged with providing export management training for SMEs.</font><hr width="90%" size="1">
