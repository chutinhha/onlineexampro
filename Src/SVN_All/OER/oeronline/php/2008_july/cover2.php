<p class="MsoBodyText"><br>
<b><font face="Verdana" size="4">Promising prospects</font></b><font face="Verdana" size="1"><br>
<br>
<b>Maqbool Hameed Al Saleh, Chairman, OHI Group of Companies, is upbeat about 
the potential of the younger generation.<br>
</b><br>
<br>
<img border="0" src="COVER-STORY8.jpg" align="right" vspace="5" hspace="5" width="194" height="250"><b>What are your thoughts about the younger generation? </b><br>
I see a lot of potential in the new generation. I think if you give your 
children the right education, guidance and exposure, there is no reason as to 
why they won’t fulfill their promise. One needs to give them complete 
responsibility and guide them properly. They should not be discouraged if they 
make a mistake as the only way to take the right decision is to learn from 
mistakes. A person who works makes mistakes, but someone who does nothing makes 
no mistakes and neither does he achieve anything.<br>
&nbsp;<br>
<b>There is a feeling that the younger generation is not as responsible or 
hardworking as the previous one? Do you agree with this viewpoint?</b><br>
I would not generalise by saying that everyone is capable or not capable. If the 
young are guided properly, they would definitely develop the capability to take 
right decisions. If a person brings in his son from day one as a director in the 
company, he would not be giving him a chance to grow. It is essential to train 
him first by getting him to work somewhere else. The first proof of his 
capabilities will be the fact that he can find a job elsewhere. Even when he 
joins the family business, he needs to be put in a place where he can grow and 
climb up the professional ladder. <br>
<br>
<b>Is the new generation more qualified and technically competent?</b><br>
It varies from person to person. You can come across someone who has an enviable 
educational degree but is absolutely useless; as also someone who is brilliant 
and from whom one can learn a great deal. <br>
<br>
<b>Can two generations work together or does it create friction?</b><br>
Two generations can work together if the older generation looks at the new as 
friends and partners. The young should be consulted and allowed to express their 
opinions. I will give you an example: we were recently planning to pitch in for 
a US$30 million acquisition, but after consulting my sons, I decided to drop the 
idea. <br>
<br>
<b>Do you see a gender divide in terms of approach to work and sensibilities 
within the next generation?</b><br>
The handicap with girls is that they get married and go away. My daughter 
started her own business and was doing quite well when she got married. Men on 
the other hand are more serious and keen on achieving bigger things. <br>
<br>
<b>Is there a variance in the attitude towards money between the two 
generations?</b><br>
Definitely. It took us ages to earn money, so it is not easy for us to spend it. 
But, since they have seen riches from the day they were born, they are more 
liberal when it comes to spending money. </font></p>
