		</font><b><font face="Verdana" size="4">Healthcare demand to shoot up<br>
		</font></b><font face="Verdana" size="1">
		<br>
		</font><b><font face="Verdana" size="4">
		<img border="0" src="gcc_dairy1.jpg" width="150" height="170" align="left" hspace="5" vspace="5"></font></b><font face="Verdana" size="1">In the wake of rapid increase in life expectancy and surge in diseases, 
		the demand for 
		</font><font face="Verdana" size="1">healthcare facilities in GCC region is expected to grow 
		by 240 per cent in 20 years according to a McKinsey report. The 
		healthcare costs in the GCC will go up from US$12 billion (currently) to 
		US$60 billion by 2025.<br>
		<br>
		“Rich countries spend a bigger portion of their income on healthcare 
		than poor countries. For example, USA spends 15.2 per cent of its gross 
		domestic product (GDP) on healthcare. In contrast, many of the GCC 
		nations that are high income countries spend less on the healthcare. 
		Oman and UAE spend just 2.5 per cent of their GDP on healthcare. In the 
		case of Saudi Arabia and Kuwait, the figures are 3.4 per cent and 2.2 
		per cent, respectively,” said Dr.Loan Cleaton-Jones. Senior Health 
		Specialist in the Health and Education Department, International Finance 
		Corporation.<br>
		<br>
		He added, “The trend is bound to change as more investments are being 
		made and will be made in the healthcare industry. For example, currently 
		the number of hospital beds in the GCC is 68,250 (2006). The figure will 
		go upto 114,450 (2015) and 161,750 (2025), indicating an increase of 135 
		per cent.”<br>
		<br>
		The global healthcare giants recogniwe the growth potential in the 
		region. GE Healthcare, for example, expects revenue of $9 billion from 
		the region by 2010. In 2002, the figure was just US$1 billion. What’s 
		driving the growth? The population aged 65 and plus is growing at a 
		faster rate than the world average. Better health facilities mean that 
		people are living longer and consuming higher number of health services 
		on account of their old age. <br>
		<br>
		</font><b><font face="Verdana" size="4">
		<img border="0" src="gcc_dairy2.jpg" width="210" height="240" align="right" hspace="5" vspace="5"></font></b><font face="Verdana" size="1">On the growth drivers, Dr. Loan further said, “There is a trend of 
		inter-regional investment by liquid investors from GCC into other 
		regional economies. The private sector is growing much faster than 
		public sector in the healthcare; and there has been a growing 
		partnership between the private and public sectors in the larger 
		interests of the industry besides increased business opportunities. The 
		governments are also encouraging the private sector to play a major 
		larger role.” <br>
		<br>
		But the road towards progress is going to be bumpy for the healthcare 
		industry in GCC. Even though the region has the financial muscle with 
		oil prices hovering around US$130 per barrel but it severely lacks 
		skilled manpower to manage and provide the services. The second major 
		challenge is the rising inflation which is leading to higher increased 
		healthcare costs. <br>
		<b>– Akshay Bhatnagar</b><br>
