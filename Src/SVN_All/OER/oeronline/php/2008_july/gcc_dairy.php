		<b>Gulf Finance House IFRS Training</b><br>
		In a continuous effort to maintain the highest global performance 
		standards, Gulf Finance House (GFH) recently hosted a three-day 
		intensive training course to ensure that staff members are up-to-date on 
		the latest International Financial Reporting Standards (IFRS). GFH 
