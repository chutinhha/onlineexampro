<p align="justify"><br>
<b><font face="Verdana" size="4">Moving with the times </font></b>
<font face="Verdana" size="1"><br>
<br>
<img border="0" src="PERSONALITY.jpg" align="right" vspace="5" width="300" height="325">In 
business, there are entrepreneurs, some of whom are initiators, others who are 
inheritors and follow the footsteps of their predecessors, and there are some 
who improve upon the legacy of their fathers. Sheikh Qais Bin Salim Al Khalili, 
Managing Director, Al Khalili Group, belongs to the last category and he is a 
man who took his family business from a small platform to a higher pedestal and 
gave it a corporate identity. In the face of multiple challenges, it was his 
strong resolution, vigour and powerful ideas that stood him in good stead.<br>
&nbsp;<br>
Over the past one-and-a-half-decade, Al Khalili Group has grown under the 
leadership of Qais into a leading multi-faceted infrastructure business house 
with multiple independent divisions–building materials, tools and equipment, 
electricals, construction, IT and telecom, aluminium fittings and real estate. 
The group today is Oman’s largest one-stop shop for building materials and 
electrical accessories. It is the country’s largest importer of timber and is 
the recipient of the Largest Importer Award from Port Sultan Qaboos. Its 
turnover, which hovered around $5 million in 1996 has reached over $100 million 
in 2008.<br>
<br>
Al Khalili was a small building materials trading company set up in 1975, when 
the country witnessed a boom in the construction industry. It had a moderate 
growth in its first two decades, seeing little diversification. Learning curve 
With a business administration degree from the US, Qais returned to Oman in 
1996. Though not keenly interested in working with building materials, he joined 
his family business without much ado as its Managing Director. <br>
<br>
In the US, he already had an orientation in the art of business. He had shuttled 
between New York and Florida to do a business in automobiles, just for a lark. 
Gregarious by nature, Qais has interacted with people from all over the globe, 
be it South America or East Asia. Within a span of four or five years, he had 
developed a fairly good understanding of the business styles of people outside 
Oman and this came in very handy when he returned to take charge of the family 
business. <br>
<br>
Qais’ family hails from the secluded Al Dakhiliyah region, historically a 
hotspot for traditional businesses. But being born and bred in Muscat, with an 
exposure to the western world, Qais developed an outlook in business, 
quintessentially different from that of his family. And it was this that made 
him inform his father that creativity or productivity of the company’s workforce 
could not be enhanced within the present traditional structure. They needed to 
be more responsible and productive for which more space and freedom was 
essential. Very skillfully, Qais built a corporate culture that empowered his 
team to transcend from a conservative set-up to a more contemporary milieu, 
essential for the company’s growth in a modern competitive world. “I have 
treated employees as partners with incentives,” affirms Qais. <br>
<br>
Al Khalili’s rapid growth can also be attributed to its policy of investing 
resources in the training of its human capital. Qais had started with a team of 
about 53-odd staff. Today, the company has over 1,000 employees. <br>
<br>
In his initial years, Qais invested his time and energy to build up a 
relationship of trust and goodwill with customers and had a close interaction 
with them. He believes that an enduring relationship is the bedrock of a 
company’s growth, eventually helping the brand to develop. Here was one 
enterprising individual who, unlike his predecessors, thought money was not the 
prime focus, but a bi-product of that relationship; if the relationship grows, 
money will also multiply.<br>
<br>
Qais took a step-by-step approach to foster growth in his company. His first 
priority was to develop the infrastructure of the Al Khalili group, which now 
owns a transport network of cranes, forklift, trailers trucks, pickups, vans and 
cars, eventually helping in operations in the construction material shops of the 
group across the Sultanate.<br>
<br>
Al Khalili’s largest division, the building material section, of which timber 
and steel are key elements, accounts for 60 per cent of the group’s annual 
turnover. Qais took an active interest in enhancing the quality of the timber, 
ensuring his workforce had skill and experience to assess and recognise timber.<br>
<br>
Working proactively, for the first time in the Sultanate, he introduced schemes 
like gold and money-back guarantee schemes for customer satisfaction, drawing 
flak from traditional industry captains. They raved that there was a new kid on 
the block and that his crazy ideas were going to land him in trouble. Qais grins 
and says, “I am still holding the ground.” It is said that an invasion of armies 
can be resisted but not an idea whose time has come. Qais was moving with the 
times. <br>
<br>
<b>Expansion drive</b><br>
Aggressive marketing combined with keen accounting policies made the group a 
corporate entity in its own right. The many divisions of Al Khalili make the 
group an integrated organisation that now handles world-class brands and 
services, all under one roof. Qais’ ability to identify market needs saw gainful 
alliances with many MNCs like UFO (UK), Clipsal, Bell, Karcher, Kemps Neon (UK), 
Luxmay (Spain) and many others. Other brands with Al Khalili group include Jotun 
Paints, Hotex water heaters, Black &amp; Decker, Piranha, Mode lighting, Wilco, 
Stanley Tools, Pro-tech and others. Representing world famous brands and 
enhancing their brand visibility in Oman and the Gulf bears testimony to the 
rising status of the Al Khalili group. <br>
<br>
Al Khalili, which had started off with just four branches, will by the end of 
2008 expand to 16, with Al Batinah, Dakhiliyah, Sur, Sohar being key points. 
Another one on the way is in Salalah. Parallel to a huge number of branches, Al 
Khalili is now creating a mega logistics centre as their support system hub, 
facilitating the distribution network. Qais is further thinking of developing it 
into an autonomous logistics centre for other companies in the region and 
thereby turning it into a profit centre. <br>
<br>
The new technology division, Khalili Technology, is another brainchild of Qais. 
It offers cutting-edge technology based total IT solutions for security systems 
and telecommunication networking among others. It caters to the needs of small 
and medium businesses, government enterprises, and educational institutes. In a 
unique approach to keep his brand visible, Qais displayed a computer-generated 
image of His Majesty Sultan Qaboos Bin Said, seven floors tall, to celebrate the 
national day. The gesture was appreciated and very well received. <br>
<br>
Good at assimilation, Qais created a construction company in 2007 as an 
integrated approach towards building solutions. The company is today involved in 
several government and private sector projects, including the Sultan Qaboos 
University and Marine College in Sohar. <br>
<br>
<b>The big picture</b><br>
The Al Khalili group is now moving beyond Oman and has opened offices in some 
GCC countries. Qais’ aggressive marketing strategies have facilitated a number 
of mergers and acquisitions. SBH lighting was launched in Dubai in 2005, which 
marked Al Khalili’s diversification into energy-saving lighting systems. <br>
<br>
In a period of expansion, price fluctuations are of raw materials posing a major 
threat to entrepreneurs. But Qais has his own ways to deal with the crisis. He 
set up Al Khalili offices upfront in Malaysia, Dubai and China to keep him 
informed on fluctuating prices. Extra manpower is engaged to analyse pertinent 
issues like soaring prices of raw materials and shipping freights. <br>
<br>
In the face of such challenges, Al Khalili wants to expand business in the UAE, 
Qatar, and Bahrain. He is not baulked by the competition thrown by regional 
companies in his home turf. Instead, he wants to develop his business inside as 
well as outside Oman to meet the competition head on. Learning is a never-ending 
process for Qais, who attends exhibitions to learn about new developments in the 
continuously evolving business environment. <br>
<br>
On the personal front, Qais can be said to be the new face of fatherhood. Like 
today’s fathers, he now chooses to be more involved with his four kids. His 
approach to life is very positive, skilfully balancing old and new. “We can’t 
ignore the vast experience of the old – just add more blood into it with new 
ideas,” he maintains. Qais did not get lost in the shadow of his predecessor or 
get stuck in a past not of his own making. He established his own name and 
identity by changing course and gained power by doing things in a progressive 
way. By thinking far ahead, he has helped determine the future of Al Khalili 
Enterprises. <br>
<br>
Always turning negatives into positives and thinking big, he says, “It is time 
for us to create our own chain or brand. It will represent our own culture, our 
way of life. We should start having it here and export it; it is time to have 
equal base with the foreign brands. If we did not have history, I would not have
<br>
said this.” </font></p>
