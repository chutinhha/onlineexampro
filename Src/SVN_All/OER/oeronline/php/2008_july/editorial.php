</font><b><font face="Verdana" size="4">Gender Matters<br>
<br>
</font></b>
		<font face="Verdana" size="1">It was heartening to 
		notice the inclusion of two Omani business women in the latest Forbes 
		Arabia list of 50 powerful women a few weeks ago. Both, Ms Manal Abdwani 
		and Ms Lujaina Darwish, deserve to be in this august club. It is for the 
		third time that Lujaina has been chosen by the venerable business 
		publication.</font></p>
<p align="justify" style="margin-left: 10px; margin-right: 10px">
<font face="Verdana" size="1">Congratulations! By emerging as a notable business 
personality despite the bureaucratic tag reveals a lot about Manal’s business 
acumen. That her’s is not just a namesake position becomes clear as one looks at 
the turnaround she brought about in Oman Flour Mills and Mazoon Distribution 
Company in her capacity as Chairperson of both companies. <br>
<br>
Lujaina’s rise to the top slot in the MHD empire under the watchful eyes of the 
one and dynamic patriarch is equally commendable. Forbes Arabia could not have 
chosen better than these two performing dynamos. <br>
<br>
Women are today visible everywhere: banks, broking houses and boardrooms as 
well. The full credit goes to His Majesty Sultan Qaboos bin Said’s foresighted 
emphasis on women empowerment. Oman’s record on the boardroom front is eminently 
noteworthy. The latest TNI Research Report on Power Matters: A Survey of GCC 
Boards conducted in association with Hawkamah: The Institute for Corporate 
Governance and IOD Mudara sheds a lot of light on where women stand on the 
corporate firmament in the Sultanate. <br>
<br>
Oman, according to the Report, occupies the second slot from the top – just 
below Kuwait. Consider this: For the entire GCC region, women’s occupation of 
board seats is estimated at 63 out of 4,254 constituting 1.5 per cent aggregate. 
Of this, Oman boasts of 21 women seats as against Kuwait’s 30. In global 
percentage terms, it is negligible, no doubt: just 2.3 per cent with Kuwait’s 
being only 2.7 percent. Yet, it is a commendable feat in this part of the world.<br>
<br>
Barring Oman and Kuwait, other GCC member countries (Bahrain, Qatar, UA and 
Saudi Arabia) could not muster a performance that went beyond the single digit 
in real numbers. Again in percentage terms, except Bahrain and Dubai, which have 
managed to remain just above one percentage point, others are in the sub-one 
percentage category. “The small representation of women on regional boards (1.5 
per cent) is unsurprising and looks minuscule compared to 13.6 percentage 
American female representation or the 22 percentage in Norway,” comment the 
report’s co-authors Amer Alawai and Brian Davidson. <br>
<br>
Nevertheless, they don’t hide their glee while turning the spotlight on Oman and 
Kuwait. “While the region appears as a global diversity laggard, Oman and Kuwait 
buck the trend to come out as regional diversity leaders – ahead of Italy and 
just a notch below Spain,” they add. Social and cultural differences may be the 
key for low women representation on the corporate firmament, but it is bound to 
change over time. <br>
