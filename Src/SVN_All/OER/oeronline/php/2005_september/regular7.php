e-business: Back to Basics<br>
</font></b><font face="Verdana" size="4" color="#FF0000"><br>
</font><b><font face="Verdana" size="1">Matt Harrison, in this concluding part 
of his two-part series on e-business, talks about how organisations could get 
the maximum benefit from effective e-business practises.</font></b></p>
<p align="justify" dir="ltr"><font face="Verdana" size="3"><b>
<img border="0" src="msm.jpg" align="left" width="150" height="163">A</b></font><font face="Verdana" size="1">t 
a recent Open House workshop ‘Website Design, Content and Technology’ delivered 
by Jamal and Anwar Al Asmi, RealityCG at Knowledge Oasis Muscat, many 
participants raised issues related to their websites, an element of e-business 
that many in attendance were involved in and concerned with. Indeed, it was 
revealed during discussions that most participants use their websites to provide 
information on their company/government ministry, its organization, products 
and/ or services. The next most important aspect was a presentation of summary: 
financial or annual report data on the organisation. And finally, explicit 
promotional materials, beyond product descriptions, are beginning to make their 
way into Oman-based websites. This use will doubtlessly increase as Internet use 
expands in the Sultanate. Given the use and importance of this area of 
e-business, some typical questions raised during the Open House included:<br>
1. &quot;We’ve a website ... how do we profit from it?&quot;<br>
2. &quot;How can we build relationships based on long-term value?&quot;<br>
3. &quot;How do we serve customers without giving away information to competitors?&quot;<br>
4. &quot;How can we catch up on competitors who’ve got there first?&quot;<br>
5. &quot;How can we avoid the most common mistakes?&quot;<br>
6. &quot;How can we capitalise on the mistakes of others ... pick up some information 
the competitors don't know they’re giving away?&quot;<br>
<br>
What people eye is an interesting and easy-to-navigate website that makes them 
feel in control. What makes them frustrated is a confusing website where they’re 
unable to respond; where the information is laid out without consideration for 
how they think; or requiring information they don't have (such as stock part 
numbers) to find things. In response to these questions, the following 10 points 
were thrown up as issues that companies and ministries operating websites should 
consider.<br>
<br>
<b>1) Say it straight</b><br>
Why would anyone want to come to your website? Where does that fit into your 
overall marketing strategy? Ask yourself &quot;Who cares?&quot; for each sentence on each 
page. It's easy to find examples of people who didn't do that - annual reports 
with the Chairman’s Address, verbose mission statements and other fillers that 
satisfy the creators but not the readers in the e-marketplace.<br>
<br>
<b>2) And what’s your name?</b><br>
Be very careful of people's privacy and security concerns. Trust, as discussed 
earlier, is fragile and needs to be earned by respecting the users who distrust 
websites that immediately ask for personal data.<br>
<br>
<b>3) Promote, promote, </b>promote Don't believe that all you have to do is 
build a website and they will come. Promote the website thoroughly from company 
stationary, advertising, side panels on company vehicles, mail lists to search 
engines. The message here: cover all bases!<br>
<br>
<b>4) Place an order</b><br>
Just as it's important for people to find your website, it's important that they 
know clearly what they should do if they want to place an order and leave 
knowing what will happen next. See the Scotland Craft Brewers Cooperative 
example below.<br>
<br>
<b>5) I want feedback</b><br>
If you don't provide an e-mail or feedback form, people come to your website, 
can't find what they want ... but you provide them with no way of letting you 
know. Furthermore, to help them before they call you, provide an FAQ - a list of 
Frequently Asked Questions and their answers.<br>
<br>
<b>6) What does it say?</b><br>
Web pages that are too dense in text or graphics, or use too many colours or 
fonts are hard on readers. Dense text is fine for directories, but not for pages 
to be actively read. Very small text is fine for twenty-somethings, but if your 
audience includes over-forties managers, they've better use for their time than 
straining over your pages.<br>
<br>
<b>7) Too flash, too long </b><br>
Large graphics may look great in your brochures, but they make people wait. If 
your page takes more than 30 seconds to load, they’re only one click away from 
leaving you. &quot;Flash&quot; websites look great to the designers, but if 
security-conscious visitors have flash or scripting disabled, all they see is a 
blank screen. Too clever is not clever.<br>
<br>
<b>8) Unclear navigation</b><br>
If your website uses only graphical image-maps, users miss the standard &quot;blue 
underline&quot; to let them know where they’ve been. Ensure the display fits in the 
typical browser window. Provide a keyword search facility - after all, you can't 
always expect visitors to structure information the same way you do.<br>
<br>
<b>9) Fresh updates</b><br>
How often is your website updated? How will your prospects know when it is?<br>
<br>
<b>10) Learn what’s what</b><br>
Spend time online and learn for yourself what works, what feels right and what's 
a turn-off. The Internet goes at a different speed than print media but courtesy 
and service are always in fashion.<br>
<br>
<b>An Internet Marketing Success Story</b><br>
With regards to Internet marketing, Scotland's Craft Brewers Cooperative (SCBC) 
is a clear example of what small and medium size enterprises (SMEs) can achieve 
collectively via the Internet. Indeed, there may be parallels to be drawn from 
the SCBC experience for those involved, for example, in attracting tourists to 
the Sultanate or the Omani products campaign. <br>
<br>
SCBC is the sales and marketing arm of the Cream of Craft Brewers in Scotland, 
that is, those who brew beer in traditional style, using no additives or 
preservatives and malt that is free of GMOs (Genetically Modified Organisms). Of 
the 22 SME brewers in Scotland, six and a bottling plant make up the 
Cooperative, the aim of which is to promote its members' product under the 
Scotland's Craft Brewers Cooperative brand name, to the large UK supermarkets. 
Individual brewers didn’t have the capacity to market these on their own. In 
order to reach a larger market, and to export beyond the UK, the Cooperative has 
developed a website, which is already breaking new ground that would be 
difficult to do through conventional marketing means. For example, SCBC’s 
products bear a generic label that can be customised by the purchaser to reflect 
an image, name, or both. The image and words are digitally transmitted to the 
Cooperative and the customisation service applies to orders as small as three 
bottles. Delivery is made anywhere in the world, coordinated by ParcelForce, and 
customers are able to use ParcelForce's Internet-based tracking service to check 
the status of their delivery. As a very small business, SCBC did not think it 
had the skills or funding to run a business on the Internet. However, by using 
the IBM HomePage Creator service, it was able to go live within a couple of days 
at a very low cost. Thanks to the service simplifies set-up, SCBC was able to 
focus on its business without worrying about the technology. As a result of its 
growing success on the web, the SCBC is predicted to create 75 new jobs within a 
year and to build a brand name rivalling better-known traditional brewers on a 
world scale. The Cooperative will continue to develop its brand and market for 
its product. It expects other brewers to join the Cooperative, in order to 
compete in a world market. <br>
<br>
<b>Education and information dissemination</b><br>
• Everyone knows about e-business - but no one can describe what it means.<br>
• Lack of understanding of what the Internet is and what can be done with it 
prevents small businesses from investing limited resources into learning more.<br>
• Small businesses need to be educated in a context they understand - with 
business models and small business cases in their own industry sector.<br>
<br>
<b>Suggested Solutions</b><br>
• Mentoring programmes.<br>
• University and college students on internships.<br>
• Have “hub companies” mentor their business partners, then use small businesses 
to mentor other small businesses.<br>
• Need to encourage co-operation among competitors.<br>
Formal Courses<br>
We need short executive-style courses specifically targeting SMEs on a 
sector-by-sector basis.<br>
<br>
<b>Ad campaigns</b><br>
Advertising to educate the public on Internet issues and increase local 
e-business activity.<br>
<br>
<b>Government push: lead-by-example: one </b><br>
An important class of large organisation that’s capable of generating the 
necessary momentum towards widespread adoption of e-business in Oman is the 
Government. The Government ministries, and this isn’t peculiar to Oman, have a 
healthy respect for their own independence and generally resist coordinated 
initiatives. Moreover, many governments that have established e-government 
initiatives have failed to pursue their strategies effectively, or failed to 
adapt them to the rapid changes in technology that have occurred; hence some of 
their projects have been stillborn.<br>
<br>
Transforming government to adopt e-government practices will help increase 
awareness and skill sets within these agencies. There’s another reason why 
government should re-examine its operations. Its role in stimulating e-business 
will be better performed if it adopts e-business for its own transactions. These 
transactions would include dealing with the general public for such matters as 
administering driving licenses, vehicle fines, business licenses and issuing 
passports. It’s important to note that much of this work is already being 
tackled by Information Technology Technical Secretariat (ITTS) at the Ministry 
of National Economy. Indeed, over the past two years, it has planned and 
executed some groundbreaking e-projects - initiatives that aren’t just a first 
in Oman but a first in the Middle East - the National ID project is an example 
of this.<br>
<br>
<b>Cost of e-business</b><br>
• Lack of understanding leads to uncertainty concerning costs. For example, a 
website can cost between RO350 and RO10,000. Clearly, you would expect to get 
more for RO10,000 than for RO350 but what exactly is a given website worth? How 
are these figures derived at? Oman’s SMEs, for instance, can’t afford to invest 
when the uncertainty of costs is so high. Indeed, in order to succeed, online 
business requires an ongoing commitment and an effort similar to that in a 
traditional business setting. Maintenance and production costs are hard to 
anticipate; especially if the website is continually adapting based on lessons 
learned. <br>
<br>
<b>Suggested solutions</b><br>
Education, but not of the underlying technical nuts and bolts is required. 
Instead, education of the business managers on how to do process re-engineering 
in order to accommodate changes brought about by adoption of e-business 
practices is the need of the day.<br>
Tax-based incentives, such as tax write-offs for website production costs.<br>
<br>
<b>Resource allocation</b><br>
• Lack of understanding by managers leads to IT staff being assigned 
inappropriate duties.<br>
• IT staff don’t typically understand business models, marketing, PR, corporate 
communications and value chains. Yet, they are given strategic control over an 
entire new segment of the organization’s operations.<br>
• Online operations typically evolve to encompass a parallel enterprise, 
mirroring all aspects of the underlying business counterpart. Without strategic 
planning, this leads to inefficiencies and cost overruns.<br>
• New activities are generated that have to be performed on an ongoing basis, 
but no one is assigned the responsibility of performing these new duties.<br>
<br>
<b>Suggested solutions</b><br>
Educate managers using business cases for similar-sized, same-sector businesses. 
Include members from all parts of the business in strategic planning meetings to 
ensure that integration of new and old business processes are handled as 
smoothly as possible.<br>
<br>
<b>Government’s role: lead-by-example: two </b><br>
• Stimulating e-business: This means what government can do to create and expand 
the positive impacts of e-business. This would include, for example, continuing 
to liberalise the telecommunications infrastructure, promoting appropriate 
venture capital arrangements and supporting SMEs. As pointed out earlier, much 
of this work is being done, but how successful have government agencies been in 
getting their success stories out to the business community and the general 
public? We’ve had successes. So, let’s shout about them. <br>
• Regulating e-Business: This refers to what government can do to combat the 
negative impacts of e-business - preventing frauds, protecting consumers and 
increasing security. Again, publicise widely what’s been achieved.<br>
• Leading by example: This is what government needs to do to move towards 
e-government. It would include making more services available on the Internet 
and moving towards e-procurement. If these services already exist, then we need 
to look at ways of broadcasting their existence to business and the general 
public.<br>
• Managing the e-economy: This refers to the management processes, structures 
and mechanisms that need to be in place for what’s generally referred to as the 
Information Age. <br>
• It’s suggested that government give greater encouragement to schemes whereby 
employers allow employees to use or borrow corporately owned computers to access 
the Internet. <br>
• It’s suggested that government work in partnership with Omantel, Oman Mobile 
and Nawras to reduce Internet access costs to the levels of the lowest, and to 
reduce mobile telephony charges to access the Internet. <br>
• It’s suggested that Omantel conduct a critical and continuing review of its 
current methods for monitoring access rates, quality of access and costs. <br>
• It’s suggested that government ensure that privileges associated with Internet 
usage apply equally to all sections of the Omani society. <br>
• It’s suggested that government consider working with schools, colleges and 
women's groups to achieve balanced usage of the Internet. <br>
• It’s suggested that government work to clarify and develop the roles of credit 
card companies and banks in Internet trading, and where appropriate, collaborate 
with e-business traders, who are seeking to improve consumer confidence. <br>
• It’s suggested that government promote an awareness of the importance of, and 
improve the mechanisms for, consulting industry, particularly SMEs and new 
economy entrepreneurs, and consumers whenever legislation is to be drafted which 
bears on e-business. <br>
• It’s suggested that government provide improved support for SMEs in respect of 
e-business cross-border trading, by providing clear and easily accessible 
information, especially on the Internet.<br>
• It’s suggested that government take steps to change organisational culture and 
attitudes within governmental departments. Options considered could include 
making Internet use part of everyday routine, incorporating Internet literacy in 
reward and review systems.<br>
<br>
<b>Wrap up</b><br>
Ideally a new Internet Service Provider (ISP) would try hard to establish itself 
and strive towards increasing customer base by targeting SMEs and individuals. 
But in Oman the Internet penetration is low and the ISP has not been able to 
stimulate the potential customer base.<br>
<br>
SMEs in Oman have been relatively slow to adopt e-business strategies. Many of 
these businesses don’t see how e-business applies to them - they need to see 
concrete business plans that are relevant to them in terms of both business 
sector and number of employees. Finding appropriate SME business cases and 
success stories from both within Oman and around the Gulf region and 
disseminating these cases to businesses would help them justify investing more 
time and effort into learning what e-business can do for them. The e-business 
isn’t a fad; it’s here to stay. <br>
The sense of urgency that exists in other parts of the world has been driven in 
part by major industry giants forcing suppliers in their supply chain to adopt 
e-business or be dropped as suppliers. PDO has worked well in this area. This 
catalyst for change needs further encouragement in Oman. <br>
<br>
Oman-based Internet marketing is moving slowly, largely because of the 
professional immaturity of those selling the concept, and to a more limited 
extent, the technical and marketing immaturity among the target audience.<br>
<br>
In partnership with a number of Oman-based private sector organizations, Omani 
government agencies are active in maturing their initial web-offerings into 
fuller, more responsive and better coordinated and maintained services. Again, 
excellent public examples of this include Knowledge Oasis Muscat, ITTS, the 
Ministry of Information and Muscat Municipality. <br>
<br>
Omani firms that use the Internet tend to be larger companies. A clear majority 
of SMEs are also users, including both industrial and service providers. 
However, access to the Internet within the firm or government body is relatively 
select, with only limited PCs connected. This suggests that Oman-based firms and 
government ministries have yet to discover wide operational need for the 
Internet in their day-to-day operations.<br>
<br>
An e-mail would appear to be to the primary benefit of the Internet to 
Oman-based companies and government ministries, and is utilised both internally 
and to communicate with outsiders. Indications are, however, that the pervasive 
use of e-mail in transmitting documents or data in Europe or the US is not yet 
as widespread in Oman, possibly because the number of sister companies and 
individuals connected to the Internet is still far less than in Europe or the 
US. In brief, the value of e-mail in business depends critically on 
inter-connectivity.<br>
<br>
In anticipation of increased Internet connectivity, Oman-based companies have 
already introduced websites. Indeed, a reasonable speculation would be that if 
communication facilities and tariff charges were improved, and further 
education, technical support and encouragement offered to the business 
community, then we would witness a marked increase in e-business activities in 
Oman. </font></p>

