<b>
<img border="0" src="personality1.jpg" style="border: 1px solid #800000; padding-left: 4; padding-right: 4; padding-top: 1; padding-bottom: 1" align="left" hspace="5" width="125" height="156"></b>Nasir 
Issa Al-Ismaily, General Manager, Export Credit Guarantee Agency (ECGA), is a 
man who believes in action. For him, no obstacle is un-surmountable. He has 
steered his organization to a position where it now entertains inquiries from 
the likes of the Dubai free zone, but he does not believe in resting on his 
laurels. He would rather go on and, set new benchmarks. <br>
<br>
ECGA, it may be noted, was the winner of the “Public Service Initiative 2004 
Award for Oman Awards for Excellence”. And Al-Ismaily was recently re-elected 
Regional Coordinator and Chairman of Africa/Middle-East Group within the Credit 
Alliance Network and member of its General Committee at its annual meeting in 
Shanghai,China. Excerpts of the interview with Al-Ismaily: <br>
<br>
<b>Tell us briefly about the role of ECGA in promoting exports.</b><br>
We, at the ECGA, play a crucial role in promoting exports through risk 
mitigation. We have been functioning for almost 14 years now; we started our 
operations in 1991. The main role of the ECGA is to promote Omani exports 
through credit insurance, guarantee and financing services.<br>
<br>
<b>What has been the impact of credit insurance on Omani exports so far?</b><br>
Credit insurance has played a very crucial role in boosting exports. Omani 
exporters have long valued our services very much. They know that if the buyer 
goes bust, or if they get into a non-payment logjam for their export shipments, 
ECGA comes to their rescue. We indemnify the risks of non-payment, and our 
policyholders (exporters) realise and appreciate the benefits for such cover so 
as to protect their receivables. It also provides them with comfort as the 
exporters are more active in not only selling at liberal credit terms to 
existing buyers in highly competitive markets but also utilizing the opportunity 
to continuously explore new buyers and in far away markets, knowing that the 
credit risks has been taken care by ECGA.<br>
<br>
<b>What types of risks does the ECGA cover for exporters?</b><br>
The Export Credit Policy issued by ECGA protects exporters against both the 
commercial that is buyer risks as well as country risks. <br>
<br>
<b>What are the buyer risks?</b><br>
Buyer risks, which ECGA provides necessary protection to Omani exporters, 
include protracted default, insolvency/ bankruptcy of the buyer, or the buyer’s 
failure or refusal to accept the goods dispatched that comply with the contract.
<br>
<br>
<b>Can you outline other scope of services offered by ECGA? </b><br>
The agency meets the needs of exporters by providing a wide range of services. 
Apart from export credit insurance, it also provides domestic credit insurance 
cover for local sales, post-shipment financing of exports as well as 
pre-shipment credit guarantee. It also assists exporters in recovering old 
out-standings and bad debts.<br>
<br>
<b>What types of risks are covered under the domestic credit insurance?</b><br>
ECGA covers the risks on the insolvency of the buyer as well as failure of the 
buyer to pay the seller of the value of goods sold and accepted by the buyer.<br>
<br>
<b>Are you focusing on some specific areas in export credit?</b><br>
The Omani non-oil export sector is quite limited. If you see the figures, the 
number of Omani exporters is relatively small compared to developed countries. 
However, the growth rate of the Omani export sector has been very remarkable. 
For example, the level of the Omani non-oil export has grown up significantly by 
more than 38 per cent between 2003 and 2004. We at ECGA are trying to nurture 
and support our exporters. We don’t give preferential treatment to any one 
particular export sector. Our facilities are available to any Omani exporter, of 
any size, operating in any industrial, and commodity or service sector. <br>
<br>
<b>Why should one go for export credit policy?</b><br>
As a manufacturer, businessman or trader, you are probably well aware that bad 
debts can arise from any customer due to a variety of reasons. No one can ensure 
100 per cent payment of the outstanding. Then, what do you do? Go for an export 
credit policy, and rest assured of the payment of the buyers approved by ECGA. 
Without the insurance cover, an exporter may not feel free. <br>
<br>
<b>How important is export credit policy as a financial tool?</b><br>
While credit protection is important, we at ECGA are aware that the exporter 
might need additional help to finance his exports. The Export Credit Policy is 
so designed to allow the exporter to use it as an additional form of security to 
its banker. And, it would not cost extra to get these financing benefits. All he 
needs after obtaining an Export Credit Policy from ECGA is to ask for a Letter 
of Authority (L/A), which will enable him to assign to his bank the rights to 
claim proceeds under as a particular transaction insured in the Policy. And mind 
you, there is no cost involved in the issuing of L/A.<br>
<br>
<b>What is the formula for fixing the premium for the exporters?</b><br>
The basis of determining applicable premium rates depends on the degree of 
risks. They are predicated by the modes of payments and country risks 
classification. Thus, for relatively secured payments such as letters of credit 
to a buyer in a particular country under category “A,” the premium charge for 
cover is less than open account terms to a country under “D” category where 
payments are not secured. <br>
<br>
<b>How often are cases of non-payment recorded?</b><br>
We have settled a number of claims. We have seen claim cases where exporters 
have dealt with a buyer for over 20 years without payment difficulties but all 
of a sudden, the buyer stopped paying due to financial distress or other 
reasons, and therefore, ECGA indemnified the exporters for such claims. ECGA has 
paid claims to exporters to buyers in a number of countries, including Italy, 
France, USA, UK, Holland, India, Saudi Arabia, Qatar, UAE, Kuwait, Jordan, etc. 
Thus, exporters have long realised the importance of credit insurance to protect 
themselves against such risks from ECGA cover.<br>
<br>
<b>How do you assess information on the buyers?</b><br>
We have various sources, including credit information agencies as well as 
exchanges of information with other ECAs in other countries. Before we 
underwrite such business or consider any request for a credit limit from an 
exporter, we need to assess the credit worthiness, payment records, financial 
position, commercial morality as well as any related information on the buyer. 
Whenever we get any adverse information about a particular buyer, we alert our 
clients accordingly. We are very frank with exporters, who, we believe, must act 
prudently.<br>
<br>
<b>What was the original idea behind ECGA?</b><br>
When we started, the idea was to encourage the exporters to insure with us. 
Initially, the exporters used to come to us because of the subsidy factor. 
However, they have realized over a period of time the importance of credit 
insurance and that it is for their own interest to protect themselves against 
bad debts.<br>
<br>
<b>Is credit insurance a new concept in a GCC country?</b><br>
Yes it is and is not as prevalent in our Region as in Western European 
countries. Businessmen have traditionally traded on trust in our part of the 
world but the attitude now is changing, considering the risks of non-payment. 
Hence, credit insurance is considered an essential tool to mitigate such risk. 
Oman is the only country in the GCC, which has its own national export credit 
guarantee agency. A number of delegates from GCC countries have visited us to 
learn from our experiences. We have also been receiving enquiries from companies 
in the UAE to avail of our credit insurance services.<br>
<br>
<b>You have recently been re-elected Regional Coordinator and Chairman of Africa/ 
Middle-East Group within the Credit Alliance Network and member of its General 
Steering Committee. How significant is this role and its benefit to the ECGA?
</b>
<br>
<br>
I am honored for the confidence placed by the members for re-electing me to this 
important position for five years now. The role of Regional Coordinator and 
Chairman of our Regional Group is a very vital one. Our Regional Group comprises 
national export credit agencies from Africa and Middle East countries, including 
the ECGA of Oman as well as multilateral ECAs from the region, including Africa 
Trade Insurance Agency, Inter Arab Investment Guarantee Agency as well as 
Islamic Corporation of Investment and Export Credit. <br>
<br>
Besides being the Chairman of Africa/Middle East Group within the alliance, my 
role is to coordinate and convey our mutual areas of interest and issues to the 
General Steering Committee of the Credit Alliance Network. The General Steering 
Committee, for which I am a representative of the regional group, comprises of 
members from other geographical regions, including Europe, Asia, and Americas. 
The agency benefits from such associations through exchanges of information as 
well as access to database of information on millions of buyers worldwide. <br>
<br>
The Credit Alliance Network, which operates under the auspices of COFACE of 
France, is now present in 93 countries, representing over 85 per cent of the 
world trade.<br>
<br>
<b>Are there any other international associations that ECGA belongs to? </b><br>
Besides being a member of the Credit Alliance, ECGA of Oman was the first Arab 
ECA being admitted in the Prague Club, which operates under the Berne Union that 
is International Union of Credit Investment Insurers since the year 2000. The 
Agency is also associated with the Inter-Arab Investment Guarantee Corporation, 
which is a regional ECA owned by the Arab countries, including the Sultanate of 
Oman.</font></p>

