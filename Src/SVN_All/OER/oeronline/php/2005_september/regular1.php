Ruins to rains</font></span><font face="Verdana" size="1"><br>
<br>
<b>For the investment company, Oman &amp; Emirates Investment Holding, corporate 
life has been a mix of marked crests and troughs. OER traces how the company has 
grown from strength to strength despite being in the red not very long ago</b><br>
<br>
