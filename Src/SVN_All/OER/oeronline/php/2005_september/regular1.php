<img border="0" src="prof.jpg" align="left" hspace="5" vspace="2" width="150" height="206">B</b></font><font face="Verdana" size="1">ig 
turnarounds always make history. And corporate turnarounds are so stunning that 
they become a part of the history itself. The recent blockbuster in Oman is Oman 
&amp; Emirates Investment Holding Co. S.A.O.G. “Do something extraordinary to create 
something extraordinary. Walk an extra mile to reap the dividends. This is the 
way to convert misfortune into a bountiful fortune,” says Dr. Hamad Hashim Al 
Dhahab, the Chairman of the company, with all smiles and a sense of fulfillment 
throbbing from his heart. <br>
<br>
From a state of despair five years ago, this investment company has catapulted 
itself to a position of strength, tremendous potential and surging growth. Five 
years ago (at the end of the year 2000, to be more precise!), the company was in 
troubled waters; it had an accumulated loss of RO17m. And, by the end of June 
2005, it had painted a new picture of itself; created a positive retained 
earnings position of RO17m, gearing the equity level at RO30m, and a strong 
investment portfolio of RO58m. Indeed an unbelievable U-turn! <br>
<br>
When asked to spell out the secret behind this turnaround, Dr. Hamad attributed 
it to the basics of business: “Any company in the world depends on three pillars 
for its success – clear vision, clear cut plans to achieve them and good human 
resources. We, too, have depended on these very factors to raise from the dust 
and to see the light at the end of the tunnel.” <br>
<br>
Oman &amp; Emirates’ profile includes profitable subsidiaries such as Majan Fund and 
the United Brokerage Company LLC (the brokerage arm of the company in the UAE) 
and also Omani Euro Food Industries Co SAOG, which is being nursed towards 
recovery. Besides, the company has associates engaged in diversified business 
and the list includes optic fiber manufacturing unit ‘Oman Fiber Optic Co SAOG’, 
bulk shipping specialist company ‘Emirates Shipping Co LLC’ in Abu Dhabi, the 
new financial warehouse ‘Financial Corporation SAOG’ (FINCORP), ‘Oman Medical 
Projects Co SAOG’, which owns the Muscat Private Hospital and tuna canning unit 
‘Dhofar Fisheries Industries Co SAOG’ at Salalah. <br>
<br>
Oman &amp; Emirates, it may be noted, has a sound investment portfolio, with mix 
between fixed and variable income earning securities. It is a government 
shareholding company, and a stock-holding company as well. The private sector 
(from Oman and the UAE) has invested 40 per cent in the company. It’s a 20:20 
ratio from both countries. And the ratio of investment of the two governments is 
30:30. “The main objective of the company is to boost economic activities and 
pep up the private sector in Oman and the UAE,” informs Dr. Hamad. <br>
<br>
“The first major task towards rejuvenation of the company that occupied our 
focus was on the investment portfolio and framing up the criteria for 
investments,” says Dr Hamad, adding: “We came out with a clear strategy with 
flexibility to grasp good opportunities. We were ready to be in tune with the 
market requirements to diversify our capital in different areas.” The turnaround 
has not been trouble-free for the company. <br>
<br>
Tireless efforts by the company in overcoming all the difficulties from 1999, 
which were caused earlier on account of both internal and external factors, 
careful redrafting of the strategic revival policies, effective implementation 
of the programmed actions, close monitoring of the operational tasks and timely 
executive decisions, have all helped in accelerating the process of development 
of the company. “Strict money control (like cost reduction, and downsizing) has 
also helped us,” admits Dr. Hamad.<br>
&nbsp;<br>
Timely interventions often do wonders. For Oman &amp; Emirates, these interventions 
came in the shape of a number of “strategic actions”. To begin with, the company 
successfully finalized the merger of its investment banking division with two 
other brokerage outfits, with the formation of one of the biggest integrated 
financial service providers, ‘FINCORP,’ in Oman. “All our financial activities – 
asset management, corporate finance, brokerage – are now handled by FINCORP, in 
which we are a partner of strength,” informs Dr. Hamad. <br>
<br>
Next, the company being bullish on stock markets in this Middle East region, 
decided to spin off the brokerage operations in the UAE in 2001 with the setting 
up of a wholly owned subsidiary ‘United Brokerage Co LLC’. Its memberships in 
Abu Dhabi Securities Market and Dubai Financial Exchange have resulted in 
enhancing the brokerage income in the UAE and paved way for en-cashing the 
buoyancy of the stock market.<br>
<br>
With a view to generating a stable avenue of income to the small investors, the 
company chalked out a strategic decision on Majan Fund, originally promoted by 
Oman &amp; Emirates. “Majan was initially a fund with life up to 2002. But we 
extended it for five years up to 2007. In future, we may extend it further, 
depending upon the situation” reveals Dr. Hamad. <br>
<br>
The timely decision to increase its stake in a shipping company in the UAE, at a 
time when the global shipping rates were going high in the graph, has really 
fuelled the profitability of the company.<br>
<br>
Besides, there were some important but good steps taken by Oman &amp; Emirates as a 
promoter towards nursing the weak project companies, which deserve special 
attention. Dr. Hamad asserts: “Restructuring of the capital, roping in new 
investors, conversion of debts into long term loans, negotiation of reduced 
borrowing rates, and reorganizing the plant operations to better utilization are 
all the sound measures taken by us and we believe that these would pave the way 
towards faster recovery of these companies.” Also keeping in line with the Exit 
policy, the company has made timely disinvestments from certain founder 
investments after establishing self-sufficiency in their operations. This was a 
step towards realizing the gains and to move on to newer requirements.<br>
<br>
In order to enhance the efficacy of the systems, the company spent considerable 
time and efforts towards streamlining the operating methods with the formulation 
of clear-cut policies and procedures, and preparation of detailed reference 
manuals outlining the guidelines for the various functions.<br>
<br>
To add to it all, the company established good corporate governance system with 
the setting up of various committees such as Executive Committee, which monitor 
the strategies and also review the implementation of the systems and procedures. 
“We have been focusing on good corporate governance,” says Dr. Hamad. “And it 
has worked wonders for our company,” he admits. <br>
<br>
Amid all these ceaseless efforts on part of the company, there have other major 
factors that steered it out of trouble, the most notable being the financial 
assistance of RO15m from the governments of Oman and the UAE, shared equally, in 
the form of interest-free long-term loan in October 2001. This, in fact, has 
removed the financial instability that was haunting around the company. Besides, 
the Oman government’s relief package to remove the woes of small trust account 
holders in 2000 has bestowed the company with unexpected gains. <br>
Then, there was this landmark decision to reduce the share capital from RO30m to 
RO7.5m in March 2004 that cleared away the accumulated losses of the company in 
one go.<br>
<br>
Oman &amp; Emirates also has had the good fortune of having a Board of Directors 
with a rich experience in diversified industries. “The various committees of 
directors are working fine with very clear cut goals, and rules and regulations, 
and reinforce safety and security in the chosen path,” points out Dr. Hamad. 
Talking about the company’s HRD policy, Dr. Hamad says, “Just give your people 
accountability and responsibility, and they will come up trumps. This is the 
guiding lamp towards penning a success story.”<br>
<br>
<b>Outlook for 2005 </b><br>
The company has netted a net profit of RO14m during the six months period ended 
30 June 2005 and that too, after making a provision of RO 433,000 towards 
doubtful receivables, thus endorsing a return of 82 per cent on the shareholders 
equity funds. It is a beginning towards consolidation and sustenance. And with 
the oil prices becoming higher, the economic conditions are becoming stronger. 
This favorable orientation is all set to boost the company’s corporate 
performance in the coming period also. <br>
<br>
However, the stock markets in Oman and the UAE, after reaching new heights, have 
witnessed some correction after June 2005. It is believed that such correction 
is only for the good, as it eliminates the overweight of certain scrips beyond 
their fundamentals and ensures a matured approach to prevail in the long run.
<br>
<br>
The company expects the investors to proceed with caution but to maintain their 
positions and interests in the stock market during the balance period in 2005. 
“Investors are becoming mature these days. Earlier, there was nobody to guide 
people about how to invest in shares and how to study the fundamentals of the 
scrips. The situation is changing now and the awareness level of the investors 
is growing day by day. This is welcome sign towards a matured market,” says Dr. 
Hamad. It is believed that there are still fair chances for the group’s 
investments to yield higher gains during the remaining months of 2005, but of 
course subject to careful monitoring of risk vis-à-vis rewards. “We hope that by 
next year our project companies will be in a much better position,” asserts Dr. 
Hamad, adding: “It’s easier to build a house but it’s more difficult to 
refurbish it.” <br>
<br>
And, does the company plan to take up new investment projects in the future? 
“Hopefully yes,” affirms Dr. Hamad. “We are now planning to join hands with more 
companies in the UAE.”
