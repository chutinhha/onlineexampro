            <font face="Verdana" size="4" color="#FF0000">The non-oil surge<br>
            <br>
            </font></b><font face="Verdana" size="1"><b>Oman’s non-oil exports 
(excluding re-exports) have registered a sharp increase, and, in a very healthy 
sign for the economy, are constantly looking northward. Mehre Alam takes a look 
at the scenario<br>
            </b><br>
</font><font face="Verdana" size="3"><b>D</b></font><font face="Verdana" size="1">iversification 
is the key to the growth of any economy. And the broader the export baskets that 
economies have at their disposal, the wider the beam they are likely to leave on 
the faces of their people. Oman, like elsewhere in the Middle East, is currently 
riding piggyback on surging oil prices.
And this piggyback ride is expected to continue for a reasonably long time to 
come. But then, when news filters out, of an expanding export basket, in the 
form of increasing non-oil exports (excluding re-exports), it is reason to 
believe that the Sultanate’s economy is gearing up for bigger challenges.<br>
<br>
The sun has been shining bright on the Omani non-oil sector for some time now. 
The level of the Omani non-oil exports (excluding re-exports) has gone up 
substantially -- by more than 38 per cent – between 2003 and 2004.</font></p>
