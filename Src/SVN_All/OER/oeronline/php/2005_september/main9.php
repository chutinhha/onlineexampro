<p align="justify" dir="ltr"><font face="Verdana" size="1"><b>LNG exports</b><br>
“When we talk of the non-oil exports, we exclude LNG. But then, we are also 
manufacturing and exporting LNG,” says Nisreen. “Imagine the figures if one were 
to add LNG exports to the non-oil exports basket,” points out Nisreen.<br>
<br>
According to the CBO, during the year 2004, 
LNG exports amounted to 6.9 million tons as compared to 6.7
million tons in the year 2003. 
LNG was exported to Korea, USA, Japan, Spain, France and Belgium. To add to it, 
216 metric tons of byproduct NGL was also exported to the UAE and India in 2004. 
And there are some really bright areas. For example, Oman ’s non-oil exports to 
Japan have also been climbing up recently. Japan, it may be noted, is one of the 
largest importers of Oman LNG since Osaka Gas signed a longterm contract with 
OLNG to purchase 0.7 million tons a year for 25 years from the year 2000. Many 
Omani companies companies are reported to be scouting for market in Japan.<br>
<br>
<b>Outlook for the future</b><br>
The outlook looks bright enough. Asserts Nisreen: “If you look at the 
performance of the Omani origin non-oil exports in the January-April 2005 
period, it is still going northward. It was more than 25 per cent in the first 
four months.” But, is it sustainable, and, for how long? “It is!” asserts 
Nisreen. Taking the point further, Al-Ismaily says, “Considering the trend over 
the last few years, it is definitely sustainable.” <br>
<br>
Then, there are the success stories, like for example, the Oman Cables Industry. 
It now supplies MV cables to the European market. The company has carved out a 
niche of its own in the European markets, courtesy the quality of its products. 
And to add to it, developed countries like the UK, Hong Kong, Singapore and 
Belgium have also begun appearing among the countries importing reexported items 
from Oman.<br>
<br>
Nisreen pinpoints some unique advantages of Oman as a reference for future 
growth. “We have at least three distinct advantages. “First, we have an inherent 
location-al advantage. Oman is being positioned as a manufacturing base, unlike 
say, the UAE, which is largely a re-exporting base. Then, the GCC customs union 
enables easy movement of goods and services across the borders. And the Arab 
Free Trade Agreement enables us to access larger markets, like Libya, Syria and 
Jordan. <br>
<br>
Our exports to these countries are shooting up continuously. There is no way it 
can see a downturn.” The journey to the expansion of the export basket has 
already begun. For example, OCIPED has 34 representatives in different 
countries. And Oman has reasons to emerge as a major player in non-oil exports – 
it has a strong manufacturing base that includes excellent ports and abundant 
raw materials. “It’s already beginning to happen,” says Nisreen. “There will now 
be an incremental growth of Omani origin non-oil exports.”</font></p>

