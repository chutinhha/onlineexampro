<p align="justify" dir="ltr"><font face="Verdana" size="1"><b>GCC-specific</b><br>
However, the major destination for the bulk of Omani non-oil exports continues 
to be the GCC market itself (see table). Saudi Arabia and the UAE continue to be 
the prime destinations for non-oil exports of Oman, in terms of both shares in 
total exports as well as the rate of growth in exports to individual countries 
(see table). It may be noted that Oman, during the year 2004, exported to as 
many as 132 countries worldwide, with the GCC economies remaining the single 
largest export destination of Omani exports. <br>
<br>
The GCC contributes 51 per cent of the total non-oil exports of Omani origin 
exports (45 per cent in 2003). After the GCC countries, the next – in terms of 
volumes – are the Arab<br>
countries (20 per cent), followed by Asia (13 per cent), Americas (8 per cent), 
Europe (5 per cent) and Africa (3 per cent). And, the US, which is in the 
process of inking a Free Trade Agreement (FTA) with Oman, accounted for only 7.6 
per cent of Oman’s non-oil exports in 2004 (see table). <br>
<br>
According to the Central Bank of Oman (CBO), advanced countries whose currencies&nbsp; 
appreciated against the US dollar (such as the Euro-area, Japan and the UK,) 
offering thereby major exchange rate induced competitive advantages to Omani 
exporters, were not among the leading export destinations for Omani non-oil 
experts. So, is the surge GCC-specific? And, does it have to do something or 
anything with quality? “Do not equate this equation with quality,” cautions an 
Omani expert, adding: “The GCC is one of the most quality-conscious markets 
across the world when it comes to export/import. We have been importing the best 
stuff from around the world for a long time. It’s not easy to export to GCC 
countries, you see,” he makes his point. “It’s only because the consumers<br>
are satisfied with the quality of goods imported from GCC countries that there 
is greater volume of intra-GCC trade,” the expert emphasizes.<br>
<br>
However, the fact remains that over 50 per cent of these Omani origin nonoil 
products are heading to the GCC markets. And efforts at expanding the non-GCC 
market will prove crucial in the long run. “The key to pep up our non-oil 
exports lies in diversifying,” says Dr. Hamad Hashim Al Dhahab, Chairman of Oman 
&amp; Emirates Investment Holding Company. Explains Nisreen, “See, it (non-oil 
exports to GCC bloc) used to be over 59 per cent earlier. It has come down to 
slightly over 50 per cent now. OCIPED is alert to this development and is doing 
its best. We keep organizing buyer-seller meets and trade delegations. <br>
<br>
This year, we are organizing a buyer-seller meet in Tanzania. Another 
buyer-seller meet is planned in Syria during December 2005. Last year, we took a 
trade delegation to Kenya. And before that year, we had taken a trade delegation 
to Yemen. We are ceaselessly trying to expand our exports from traditional 
markets like the GCC to non-traditional markets like Arab and East Africa.”<br>
<br>
There is no doubting that Oman has traversed a long way. “There was a time when 
Oman was importing virtually everything,” points out Al- Ismaily. “It’s all 
changing thick and fast,” he adds. “However, if the bulk of our exports go to 
GCC countries, it is fairly understandable in view of the geographical factor,” 
he adds.</font></p>
