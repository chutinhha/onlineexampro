<p align="justify" dir="ltr"><font face="Verdana" size="1"><br>
“This is the beginning of a new dawn,” says Nisreen Jaffer, Acting Director 
General for Export Development, OCIPED (The Omani Center for Investment 
Promotion &amp; Export Development). The mood is quite upbeat, with things looking 
up like seldom before. Nisreen attributes this turnaround to focused and 
sustained efforts of the government and government agencies.<br>
<br>
“This is part of our export strategy. From 1999 onwards, we have been reviewing 
and updating our export strategy every year. And our efforts are paying 
dividends”. The timing of this turnaround has special significance for the 
Sultanate. Brand Oman is gearing up to cement its place as a top investment 
destination. Says Nasir Issa Al-Ismaily, General Manager, Export Credit 
Guarantee Agency (ECGA): “It (surge in non-oil exports) is a very important 
development for Oman at this point of The total exports of thrust products have 
increased from RO 200 million in the year 2003 to RO 265 million in the year 
2004. The export of product other thatn the thrust products have also registered 
impressive growths time. Earlier, the emphasis was on import substitution, when 
the country was in the process of establishing; when it was in the process of 
setting up its industrial projects.” <br>
<br>
At a time when most GCC countries are moving towards (or have moved many paces 
closer) carving out alternate models to cut dependency on oil, there can’t be a 
more welcome sign for the Omani economy. As per the Foreign Trade Statistics for 
2004 publication, the total Omani non-oil exports (including re-exports) in the 
financial year 2004 amounted to RO958.5m compared to RO904.9m in 2003, thereby 
registering a rise of RO53.6m, thanks mainly to the increases in Omani non-oil 
exports (excluding re-exports). If we juggle with the figures for Oman’s total 
non-oil exports (excluding re-exports) during the year 2004, it comes to 
RO420.3m, compared to RO304.1m in 2003.<br>
<br>
<img border="0" src="nissre.jpg" width="119" height="212" align="right"><b>Main Drivers:</b><br>
But, what are the main drivers behind this surge? For one, base metals and 
articles thereof ruled the roost, witnessing a phenomenal 81.5 per cent growth 
compared to the year 2003 (see table 1). The next is the basket of plastic, 
rubber and articles thereof, registering 64.9 per cent over 2003 (see table 1), 
followed by live animals and animal animal products (49.2 per cent), and animal 
or vegetable fats and oils that leapfrogged by 44.4 per cent in the year 2004.<br>
<br>
As for the basket of foodstuffs, beverages, tobacco and related products, it was 
“slow and steady like it has always been”, to quote an Omani expert. This is one 
sector where any downturn is least expected, but at the same time, the growth 
has not been anything to beat the drum for. The basket of foodstuffs, beverages, 
tobacco and related products registered a 26.8 per cent increase in 2004 over 
the year 2003.<br>
<br>
The sector that registered a negative growth (-8.1 per cent) in the year 2004 
was textiles and articles thereof. In fact, it has witnessed a steady decline 
since the year 2000 (see table 1). But the reason for this is not difficult to 
dig out. “It is mainly due to the abolition<br>
of the US quota,” says Nisreen, adding: “They were surviving mainly because of 
the quota regime.” The fact is that in the post-quota competitive environment, 
the Sultanate’s textiles and garments sector will have to specialize in order to 
survive and thrive. The World Trade Organization, it may be mentioned, has begun 
phasing out of garments quotas, known as multi-fibre agreements, with a view to 
creating a level playing field for all manufacturers worldwide.<br>
<br>
Interestingly, 26 products contributed 63 per cent (RO266m) out of the total 
non-oil exports of RO420m in the year 2004. These thrust products (see table 2) 
have registered a growth rate of 32 per cent. Fish and fish products, it may be 
noted, have always held great potential as far as Omani origin non-oil exports 
are concerned. Traditionally, fish exports have ranked second after oil exports 
and the first among non-oil exports as far as earning foreign exchange to the 
national economy is concerned. By 2020, this sector (fisheries) is expected 
contribute around 2 per cent to GDP (Gross National Product). And, it was pegged 
at 0.6 per cent of the GDP in the year 2000 (Ministry of Development, 2001).<br>
<br>
However, if the Omani fish exports have to penetrate the lucrative markets of 
Europe, Japan and North America, the quality of seafood will hold the key. In 
fact, the pitch also lies in more aggressively exploring the Asian and African 
markets, considering the sheer<br>
size of these markets. However, things have started to look up. Says an Omani 
expert: “Oman fish is now being exported to countries like France, Italy and 
Spain. It’s a high<br>
quality product that is being sold at high prices. Europe, in any case, is 
highly quality-conscious market.<br>
<br>
Similarly, Oman juice products are being exported to countries like India, and 
this is no mean achievement, considering that in such a huge and competitive 
market like India’s, local beverage products are plain too many. But Omani 
beverages have started penetrating these markets.” </font></p>
