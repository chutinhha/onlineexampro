<br>
Ace business planners<br>
<br>
</font><font face="Verdana" size="1"><b>OER spoke to two senior management 
professionals - Dominic Myers, General Manager of Matrah Cold Stores LLC and 
Raghavan K. Murti, CEO of United Finance Company, to understand the importance 
and process of business planning in their respective companies.</b></font></p>
<p align="justify" dir="ltr"><font face="Verdana" size="3"><b>W</b></font><font face="Verdana" size="1"><b>hen 
do you start the process of business planning for the next financial year and 
how much time do you take to finalize the business plan?</b><br>
DM: We issue our budget guidelines to managers in August. We seek to have a 
series of reviews completed by September-end with financial estimates compiled 
by mid October for presentation and confirmation by our parent company, WJ 
Towell and Co. LLC by end October/early November. Our financial year starts in 
January.<br>
<br>
RKM: At UFC, business planning starts five years in advance. We have a 
well-documented 5-year plan which is updated and revised every year for rest of 
the tenor. Generally, annual business plan would start by the middle of August 
and all operational heads would have their objectives for next year approved by 
the board and management by the end of November.<br>
<br>
<b><img border="0" src="manag1.jpg" align="left" width="120" height="171">What 
is the process of business planning in your organization and how has the process 
changed over the years? </b><br>
DM: We find that as well as aligning ourselves with our business partners it is 
vital that we invest quality time in:<br>
• Understanding potential environmental changes e.g. Omanisation, minimum salary 
levels, changes to universe/customer base, competitor activities, foreign 
exchange rates, inflation, bank interest rates.<br>
• All plans must seek to assist the delivery of the company vision and its 
component mission objectives.<br>
• In agreeing the key assumptions that will drive sales, investment, resources 
and people development.<br>
• Understanding the critical issues that have the potential to harm or drive the 
business.<br>
<br>
We spend more time these days on the driving forces that will grow or expand the 
business rather than trying to be too precise with every overhead line. Also, 
more time is now spent on channel contribution as key accounts have become more 
important and sophisticated.<b><img border="0" src="manag2.jpg" align="right" width="120" height="171" hspace="5" vspace="2"></b><br>
<br>
It is of course important that we deliver on commitments made to our 
shareholders so with the close assistance of our Director-in-Charge, Mr. Imad 
Sultan, we do not produce unwelcome surprises in either delivery of current year 
or future planning. <br>
<br>
With the support of our shareholders, we have been encouraged to be less risk 
adverse and to seek growth. This effects our planning, attitude and investment 
plans.<br>
<br>
RKM: Business planning at UFC has three stages and processes. Five-year plans 
are derived by the top management, based on macro level economics of the economy 
and our market segments. It’s a top down approach that guides the operating 
units of management perceptions and expectations. A derivative of the 5-year 
plan is the annual plan. Annual plan is drawn on the basis of bottom up 
approach. Every unit head spells out in great detail his assessment of economy 
related to the market segment he operates. Based on that, he assesses overall 
market size and determines his objectives for next year. Resource planning is an 
integral part of business plan. Manpower needs, capital expenditure, promotional 
and advertising support are all looked into. Unit heads even reassess 
operational processes to see whether operations can be improved to achieve more 
efficient servicing to gain a cutting edge for business optimization. Business 
plans received from unit heads are reviewed and mapped to corporate objectives 
by top management and centralized resources like equity, debt, funding, dividend 
etc. are decided to give a complete structure to the next year’s strategy and 
business plan. Once annual plan is frozen, following four years in the 5-years 
plan are also reviewed and readjusted to ground realities. <br>
<br>
<b>What are the crucial elements that should be taken care of while doing 
business planning? </b><br>
DM: Managers need to spend quality time on their budgets, where possible zero 
based approaches are always far more helpful than incrementalisation. Our budget 
guidelines provide for a detailed process to follow, which if adopted, helps us 
produce a more consistent presentation and execution. <br>
<br>
However, we are increasingly finding that budgets are a guideline and challenge, 
and provide a comparative base but with business moving rapidly, opportunities 
developing and regulatory requirements changing that we must not be restricted 
by the budget assumptions (however well thought out). Budgets set in October 
this year are unlikely to accurately predict specific results in December 2006, 
a period 14 months later. <br>
<br>
RKM: In my opinion, correct assessment of market size and planning for resources 
are the key to a successful plan. Competent staff, efficient workflow, user 
friendly technology and adequate liquid fund planning are absolutely essential. 
With global economies getting knowledge driven, the ability to source right man 
for the right job at right price is an essential driver for the success. A 
market research on customer aspirations for products and services and gap 
analysis between existing offerings and customer aspirations is equally 
important.</font></p>

