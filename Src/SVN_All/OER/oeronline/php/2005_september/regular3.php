<p align="justify"><font face="Verdana" size="1"><b><font color="#FF0000">New 
age robotic vacuums by LG</font><br>
</b><br>
<img border="1" src="tech_tonic_newage.jpg" align="left" hspace="5" width="100" height="66">LG 
Electronics has introduced robotic vacuum cleaner Roboking, positioned as 
high-end automatic domestic cleaner. Short battery life has been a drawback of 
robotic vacuum cleaners, but LG’s Roboking cleaner has a battery life that is up 
to eight times longer than the previous models. Its suction power is up to ten 
times stronger as well. C H Lee, President, LG Electronics Gulf FZE, informed: 
“Roboking raises the bar for all robotic vacuum cleaners. It offers great 
performance, underlining LG’s commitment to innovation and technological 
excellence.” <br>
<br>
LG claims that Roboking drives itself better than any other vacuum cleaner by 
using a unique direction-adjusting gyro sensor. The gyro sensor, a devise used 
in aircraft and satellite navigation, helps the 5.2-inch cleaning appliance find 
its way into every corner of the house. With Roboking’s onboard BLDC (Brushless 
Direct Current) motor, suction reaches 100 watts. Most competitor high-end 
robotic vacuums deliver suction power between 30 to 70 watts of suction power. 
The Roboking drives smart with the help of numerous onboard sensors that move 
the device in various set patterns. This allows the LG Roboking to drive around 
the house without getting stuck on furniture. Other features include a lithium 
polymer battery (Li-PB) that charges in approximately 2.5 hours and delivers an 
average battery life of three to five times longer than the competition. 
Roboking uses a five-step filtering system and a valve that prevents the 
backward flow of dust collected in the appliance. For the first time in the 
industry, the system incorporates two advanced HEPA (High Efficiency Penetration 
Air) micro filters. LG sold vacuum cleaners worth US$27 million in the Middle 
East and Africa in 2004, a 35 per cent increase over 2003.</font></p>
