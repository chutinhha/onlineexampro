CyberGuard launches new desktop security appliance for SMEs</font><br>
</b><br>
<img border="1" src="tech_tonic_cyberguard.jpg" align="left" hspace="5" width="125" height="44">CyberGuard, the IT security solutions company, has introduced a new 
all-in-one, centrally managed desktop security appliance to protect small and 
mid-sized enterprises against both external and internal threats to their 
network. <br>
<br>
Positioned as a robust network security solution that unifies layers of defense 
and response mechanisms, the CyberGuard SG580 provides Unified Threat Management 
(UTM) functionality, including a powerful stateful-inspection firewall, 
service-based intrusion detection blocking, anti-virus protection, and 
threat-containment through Security Policy Enforcement (SPE). UTM is a new 
category of security appliances that blends multiple security features in a 
single platform. As external and internal threats grow more complex and 
malicious, UTM appliances, with the significant improvements in performance, 
value and simplicity, offer small and medium-sized enterprises the necessary 
protection to thwart today’s blended threats. <br>
<br>
“The SG580 provides comprehensive network security for the small and 
medium-sized firms in the region by consolidating an array of attack-prevention 
tools such as network firewall, sophisticated intrusion-prevention, secure VPN 
access, A/V and web content filtering onto a single device, and makes it 
affordable for small and medium-sized enterprises the kind of security that was 
once the preserve of large corporations with large security budgets,” said 
Bashar Bashaireh, Regional Manager Middle East and North Africa, CyberGuard. <br>
<br>
“As attacks become more complex and multi-layered, it becomes imperative that 
the offices of small to medium-sized enterprises, as well as branch offices of 
large enterprises, can conduct business in a secure environment, safe from 
internal and external threats to their network.”</font></p>
