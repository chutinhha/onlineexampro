Long way to go</font></b><font face="Verdana" size="4" color="#FF0000"><br>
</font><font face="Verdana" size="4" color="#FF0000"><br>
</font><b><font face="Verdana" size="1">Salem Ben Nasser Al Ismaily, in this 
concluding part of the two-part series analysis, focuses on the progress made by 
the Arab World on the socio-economic front to catch up with the rest of the 
world.</font></b></p>
<p align="justify" dir="ltr"><font face="Verdana" size="1"><b>
<img border="0" src="sp.jpg" align="left" hspace="5" vspace="2" width="200" height="139">The 
Deficits</b><br>
An analysis of the global Human Development Index (HDI) reveals that the range 
of disparity among Arab countries is almost as large as that for the entire 
world. Bahrain, which scores the highest in the Arab world (0.843) is ranked 
40th compared to the world leader Norway (0.956), while Djibouti (0.454) ranked 
154 is close to Sierra Leone (0.273), the country with the world’s lowest HDI 
value.<br>
<br>
Despite differences across the region, three critical deficits face all Arab 
countries: freedom; women’s empowerment, human capabilities and knowledge 
relative to income.<br>
<br>
<b>Freedom</b><br>
Using the freedom index to characterise the extent of freedom in Arab countries 
compared to the rest of the world, it is revealed that out of the seven regions 
of the world, Arab countries had the lowest freedom score in the late 1990s. 
This low level of freedom in the region is confirmed by another set of 
indicators of “voice and accountability.” This set includes a number of 
indicators measuring various aspects of the political process, civil liberties, 
political rights and independence of the media. The Arab region has the lowest 
value among all regions of the world with regard to voice and accountability.<br>
<br>
Political participation in the Arab region is still limited, compared to other 
regions in spite of noticeable improvements in some Arab countries in the last 
quarter century.<br>
<br>
Civil society actors continue to encounter several external constraints in 
playing their role effectively. Bureaucratic constraints in the form of control 
of civic associations by public authorities present serious problems. The 
attitudes of Arab public authorities range from opposition to manipulation to 
“freedom under surveillance.” The media is at best partly free.<br>
<br>
<b>Women’s Empowerment</b><br>
Utilisation of Arab women’s capabilities through political and economic 
participation remains the lowest in the world in quantitative terms. Women 
occupy only 3.5 per cent of all seats in parliaments in Arab countries compared 
to 11 per cent in sub-Saharan Africa and 12.9 per cent in Latin America and 
Caribbean countries. <br>
<br>
In many countries of the region, women suffer from unequal citizenship and legal 
entitlements. In some countries with elected national assemblies, women are 
still denied the right to vote or hold office. And one in every two Arab women 
can neither read nor write. Society as a whole suffers when half of its 
productive potential is stifled. These deficits must be addressed in every 
field: economic, political and social.<br>
<br>
<b>Knowledge</b> <br>
Over and above the capability gap caused by large-scale illiteracy and 
deficiencies in the education system, serious knowledge deficits include weak 
systems of scientific research and development, and low access to information, 
communications and technology (ICT). Scientific expenditure in Arab countries 
was less than 0.5 per cent of Arab GDP for 1996, compared to 1.26 per cent for 
Cuba and 2.9 per cent for Japan in 1995. <br>
<br>
Investment in research and development (R&amp;D) is less than one-seventh of the 
world average. The Arab region also has the lowest level of ICT access of any 
world region: only 0.6 per cent of the population uses the Internet, and 
personal computer penetration is 1.2 per cent.<br>
<br>
The digital gap between Arab countries and the advanced world has widened. This 
can be attributed to the nature of the Arab ICT industry, which is highly 
susceptible to monopoly and merger, the high costs of building infrastructure, 
the larger Arab brain drain, and the growing imbalance in the distribution of 
wavebands between advanced and developing countries with regard to 
telecommunications. Cyberspace is becoming a crowded place and powerful players 
are taking up much of the room.<br>
<br>
There are also information disparities between Arab states, even when levels of 
human development are similar. In addition, there are factors widening the 
digital gap within each Arab country, with language being the decisive factor. 
Current Arab policies to address the divide focus on infrastructure, especially 
in the field of communications. Although helpful, such attempts will not yield 
the desired benefits unless equal attention is paid to the element of content. 
Most of the material on the Web is in English, a language spoken by few in the 
region. The dearth of Arabic material on the Net will continue to deprive Arabs 
of the benefits of the information age even if access itself improves.<br>
<br>
<b>The Way Forward</b><br>
The resources for development exist in the Arab region. The question is: where 
should they be placed to maximum effect? According to the 1996 Human Development 
Report, every country that sustained both human development and economic growth 
accelerated advances in human development first, or pursued both objectives 
simultaneously.<br>
In the Arab region, three areas stand out as investment priorities: Building 
Arab capabilities and knowledge; using human capabilities through 
re-invigorating growth and productivity; and promoting good governance.<br>
<br>
<b>Building Arab Capabilities and Knowledge</b><br>
Several strategies are proposed, beginning with the foundations. The report 
calls on governments to ensure 100 per cent enrolment in basic education, 
increase mandatory schooling to 10 years, support self-learning and life-long 
education, and forge tighter links between school life and the realities of life 
outside the schoolroom. <br>
<br>
It also points to the opportunity to further Arab cooperation in education, 
particularly in higher education, curriculum development, textbook production 
and teacher training - all areas where a common language presents advantages.<br>
<br>
A key driver of change would be a transformation in attitudes and societal 
incentives to support greater creativity and innovation. The rules of the social 
game need to be modified to provide incentives for embracing change and to 
afford real opportunities for social recognition, renewal and advancements to 
those who work in areas of knowledge.<br>
Investing in knowledge begins by setting a clear agenda for investment in 
science and technology and R&amp;D. The share of R&amp;D should be increased from the 
current less than 0.5 per cent of the GDP to 2 per cent by the end of the 
decade. The region can capitalise on economies of scale while addressing 
pan-Arab concerns, for example, in areas relating to water scarcity, oil, 
health, codes and standards, and other common needs. Arab governments and 
institutions should tap the expertise of the one million highly qualified Arabs 
working in industrialised countries.<br>
<br>
The region can build on recent country experiences in ICT - the Dubai Internet 
City, and the Smart Village in Egypt - in a drive to spread information and 
communication technology. In addition, the telecommunications sector must be 
reformed to guarantee deregulation and open up competition to encourage local 
and foreign investors. <br>
Another priority area is investing in specialised computer personnel and 
Arabic-language software for general education, adult training and life-long 
learning - linking this to the actual needs of the labour market. Most 
importantly, special efforts are needed to reach women through ICT and to invest 
in building up their expertise in this technology.<br>
<br>
Policymakers in the Arab world have so far not taken the most important 
component of the information industry, the element of content, seriously. Hence, 
it calls for concerted efforts to develop content in Arabic. Digitising aspects 
of cultural heritage such as text, film, music, radio and television recordings 
should become a priority. The way forward lies in giving Arab artists, 
professionals, scholars, students, entrepreneurs and other social groups 
incentives to publish and popularise their work on the Internet, rather than in 
trying to decree certain types of content. <br>
<br>
Freedom to choose what to publish and to associate with other users will drive 
the Arabisation of information content faster and more surely than any type of 
compulsion. The media has already taken the lead in this respect by placing 
Arabic newspapers on the Web.<br>
<br>
<b>Using Human Capabilities by Reinvigorating the Economy</b><br>
Achieving sustained and equitable economic growth is intimately connected with 
both the process and the goals of human development. Renewed growth is a 
necessary, although not a sufficient, condition to meet the challenge of 
employment, fully use human capabilities and overcome poverty. Reinvigorating 
Arab economies requires a multi-sector approach, and one that relies heavily on 
strengthening human development.<br>
<br>
There is a need to mobilise the private sector within an enabling policy and 
regulatory environment. In addition, the private sector should be engaged, 
together with government and academia, in R&amp;D, and focus on human-intensive 
rather than capital-intensive R&amp;D, particularly “knowledge sciences” such as 
information technology, mathematics, theoretical and device physics, and 
economics. <br>
<br>
The governments should disengage from productive activities while strengthening 
their regulatory role to ensure openness and competitiveness. The objective 
should be to encourage global and not just regional competitiveness. The bottom 
line is that domestic markets are too small to provide the basis for sustainable 
growth based on manufacturing and services. For this reason, the most viable 
response to globalisation is openness and constructive engagement in which Arab 
countries both contribute to, and benefit from globalisation. <br>
<br>
Hence, the goal of inter-Arab integration has become critical, to survive and 
compete, including through creation of a customs union or a common market. Arab 
cooperation can bring added value if the region focuses on a small number of 
specialised centres of excellence in fields where distinctive capabilities 
exist. These include water desalination in the Gulf States, computer programming 
in Egypt and Jordan, the phosphate industry in Morocco and Tunisia, or 
petrochemicals in Saudi Arabia.<br>
<br>
The legitimacy and strength of states and their institutions are inextricably 
linked to their capacity to mobilise and be mobilised in the fight against 
poverty. This implies that it is essential to mainstream human development and 
poverty reduction within national economic policy. Human development is vital to 
the region’s success in reinvigorating economic growth. The basic priority for 
policy in Arab countries is to create a virtuous cycle whereby economic growth 
promotes human development and human development in turn promotes economic 
growth.<br>
<br>
Policies that discourage employment creation should be reversed as part of a 
move towards full employment. Necessary actions include: monitoring the labour 
market and supporting measures for pro-poor human capital accumulation through 
education, training, and healthcare systems and institutional reform.<br>
<br>
There is also an urgent need to reverse the feminisation of unemployment by 
removing gender bias in labour markets, including gender-based occupational 
segregation and wage differentials, and by addressing gender gaps in the quality 
and relevance of education and training. <br>
<br>
Similarly, in efforts to strengthen local capacity to deliver micro-finance 
services beyond the less than 2 per cent of poor households that can currently 
access financial services, priority must be given to women entrepreneurs. 
Overall, social safety nets must be expanded and transfer programs increased 
from 0.2 per cent to about 1 per cent of GDP.<br>
At the same time, steps need to be taken to ensure that growth does not harm the 
environment, and we need to work for the environmental protection and 
regeneration.<br>
<br>
The Arab world suffers from the traditional destructive impact of conflicts. 
They are not only human disasters and sources of volatility and political 
instability; they are also a major constraint on high and sustained growth. 
Resolving these conflicts would obviously require a just and comprehensive 
resolution of the Arab-Israeli conflict, which is at the core of the region’s 
political crisis. In addition, addressing the damage wrought by civil wars 
requires a fundamental rethinking of how to respond to, and reconcile, cultural 
and religious groups in the Arab world.<br>
<br>
Worldwide empirical research has shown that prosperity, when based on the talent 
and work of individuals and families through free markets, leads to peace. The 
proven path to prosperity is through economic freedom, founded on property 
rights and the rule of law, which unleashes the drive and ingenuity of 
individuals. The prosperity built on economic freedom gives individuals and 
families, reason to value what they have and hope for even better things in the 
future and therefore, to despise the destruction that war brings.<br>
<br>
Enhancing social cohesion in the Arab world will not only yield large dividends 
for societies and economies; it will also help them to be resilient in the face 
of increased economic shocks associated with globalisation. Strengthening 
cohesion requires improving political rights and political, social and economic 
participation. It also calls for policies of inclusion and the expression of 
solidarity with the poorest segments of society.<br>
<br>
<b>Promoting Good Governance</b><br>
Higher levels of human development cannot be achieved in Arab countries without 
improving key aspects of governance systems. This would require reforming the 
essence of governance: i.e. state institution; and activating the voice of the 
people. The most important of state institutions is that of representation and 
legislation which provides the basic link between the governance regime and the 
people. Liberating human capabilities in Arab countries requires comprehensive 
political representation in effective legislation based on free, honest, 
efficient and regular elections.<br>
<br>
Reforming public administration is also a central and urgent task for Arab 
countries. Governments need to perform their functions as providers of public 
services and enforcers of contracts, in an effective, efficient and transparent 
manner. Public sector institutions need to be reformed to encourage 
private-sector investment and growth, to curb monopolies and to end graft and 
cronyism.<br>
<br>
Legal reform should be at the core of institutional reform in Arab countries 
because the rule of law embodied in legal and judicial institutions, is the 
foundation on which all other societal and governance institutions are built, 
including fair and honest political representation and effective and responsive 
public administration. <br>
<br>
The reform needs to focus on ensuring that the law and associated administrative 
procedures guarantee citizen’s rights and are compatible with fundamental human 
rights, particularly the rights to freedom of expression and freedom of 
association for all, under the aegis of a truly independent judiciary that 
impartially enforces the rule of law. <br>
<br>
Liberating human capabilities requires activating the voice of the people. This 
requires moving productively forward in three critical areas: strengthening 
institutions of local governance; liberating civil-society organizations; and 
fostering free and responsible media. Promoting local governance involves far 
more than the mere decentralisation of central government control mechanisms.
<br>
<br>
It should provide opportunities for more effective participation by local 
communities, particularly the poor, in combating poverty broadly defined, 
notably through efficient, pro-poor provision of basic services such as 
education and healthcare. In order to empower Arab civil society, two reforms 
are essential. First, legal and administrative obstacles hampering the 
establishment and effective functioning of civil society institutions need to be 
removed. Second, civil associations themselves need to be transformed into 
widespread popular movements, undertaking sustainable collective action.<br>
<br>
There are examples on which to draw in reforming the political institutions of 
the state. The most powerful example on which to draw in activating the voice of 
the people comes from women’s associations in the Arab world, which have 
effectively raised and lobbied against formerly taboo subjects.<br>
<br>
Full respect for human rights and freedom are the cornerstones of good 
governance that can unleash creativity and serve empowerment and participation, 
leading to human development.<br>
<br>
The fundamental choice is whether its trajectory will remain marked by inertia, 
as reflected in much of the present institutional context, and by ineffective 
policies that have produced the substantial development challenges facing the 
region; or whether prospects for an Arab renaissance, anchored in human 
development, will be actively pursued.</font></p>

