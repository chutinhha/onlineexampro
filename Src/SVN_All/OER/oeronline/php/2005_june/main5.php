<p align="justify" dir="ltr"><font face="Verdana" size="1">The GCC has also 
unified a number of trade regulations such as the Unified Guiding Regulation of 
Commercial Agencies, The Model Regulation of Trademarks, The unified Law of 
Commerce, the unified Commercial Registry Law and the unified Law of Customs. 
Several preliminary initiatives would ultimately result in the creation of the 
Gulf Common Market.<br>
<br>
The GCC Customs Union, formed in January 2003, provides for a unified Customs 
tariff of only five per cent on goods imported into any of the GCC countries and 
their subsequent movement within the region. Not only the import needs of 
manufacturing industries have been kept in view, but over 400 items have been 
included in the unified exemption list. Creation of a monetary union with a 
common currency is on the agenda as well. Other regional initiatives include a 
common GCC electricity grid and pipeline network to carry natural gas to 
consumers in the region.<br>
<br>
The GCC has agreed to permit national banks of member states to open branches in 
other member countries. Gulf Network of the National ATM Networks has been 
established and improved the centrality of Risks Law, the efficiency of capital 
and risks of Assets Law and the credit concentration in the GCC Banks Law. <br>
<br>
There is now a unified approach at the Annual Meetings of IMF and the World 
Bank. The Gulf Investment Corporation aims at supporting and developing GCC 
economic and financial resources. Model regulation for promotion of foreign 
investment in the GCC has been endorsed along with unified policies in the areas 
of agriculture and water leading to joint projects.<br>
<br>
There is coordination in setting up infrastructure projects such as seaports, 
airports and highways. There is a unified treatment to passenger and cargo 
movements within the GCC territory with exemption from all duties and taxes. An 
agreement has been reached to allow steamers, ships and boats and their cargoes 
belonging to the member states to freely use the port facilities. National 
airlines are able to sell directly in the GCC countries without the need for 
general agents of local sponsors.<br>
<br>
GCC countries have a population of 31 million, with 70 per cent under the age of 
25, 60 per cent under the age of 21 and about 50 per cent under the age of 15. 
The demographic structure is both a challenge and a provider of unprecedented 
opportunities in the region, which include issues pertaining to education, 
housing, availability of food, and ever-growing requirements of infrastructure. 
Social policy change is an important area. The demand for quality education and 
training, healthcare, and entertainment is expected to go up immensely.<br>
<br>
The population also constitutes a potential reservoir of human resources, which 
can be highly productive through the skills in information and communications 
technology to which they would be exposed at a young age. If it is to be looked 
at as a market potential, then it is a population with the highest disposable 
income in the developing countries. Other areas that would see substantial 
growth are food, beverages and pharmaceuticals.<br>
<br>
There is a coordinated approach in the area of science and technology, with 
stress on joint programmes and centres covering various scientific and technical 
spheres. A patent regulation has been ratified and GCC Patent Office 
established.<br>
<br>
Education and social fields have seen changes as well – curricula in the fields 
of mathematics and science have been unified. The Arab Bureau of Education for 
the Gulf States has been established in Riyadh and Arab Centre for Educational 
Research in Kuwait. Their objective is to achieve educational equality for all 
GCC citizens. <br>
<br>
There is promotion of social care, security, aid and development. The Executive 
Bureau of the Ministers of Labour and Social Affairs Council has been entrusted 
with the responsibility of designing a comprehensive social strategy for the GCC.
<br>
Preparatory steps have been initiated to adopt a unified modern legislation for 
rehabilitation and employment of the handicapped.<br>
<br>
There is a strategic framework – General Policies and Principles for Environment 
protection that aims at formulating proper scientific formulae to protect the 
environment and maintain the natural resources in the member states. A unified 
regulation for protection of Wild Life, Environment Impact Assessment, Handling 
Radio-active materials, Waste Recycling and transfer of hazardous waste through 
GCC borders has been ratified.<br>
