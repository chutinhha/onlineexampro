<p align="justify" dir="ltr"><b>
<img border="0" src="bilate1.jpg" width="200" height="118" align="left"></b><font face="Verdana" size="3"><b>I</b></font><font face="Verdana" size="1">ndia 
and GCC (Gulf Cooperation Council) countries, viz. Bahrain, Kuwait, Oman, Qatar, 
Saudi Arabia and the United Arab Emirates, have close cultural and economic 
relations. The GCC countries are of prime economic, political and strategic 
importance, and are now India’s second largest trading partner after the US with 
a trade turnover of about $6,802 million (RO2,616 million) annually. <br>
<br>
Over 3.3 million Indians, the largest expatriate community in the region, live 
and work in the GCC, annually repatriating nearly $5 billion (RO1.92 billion). 
Two-thirds of India’s oil imports are from this region that not only has the 
advantage of geographical proximity, but also has the quality of crude oil to 
suit Indian refineries. Cooperation in the energy sector has been the hallmark 
of India-GCC ties, and India stands to be one of the largest markets for GCC 
energy products in years to come.<br>
<br>
The liberalisation of Indian economy has yielded substantial benefits to the 
GCC. According to the Ministry of Commerce, Government of India, GCC exports to 
India have increased from $1,565.95 million (RO602.29 million) in fiscal year 
2001-02 to an estimated $1889.25 million (RO726.63 million). Indian exports to 
GCC countries during the same period have increased from $3798.05 million 
(RO1460.79 million) to $4913.03 million (RO1889.63 million). <br>
<br>
The UAE has the distinction of being the largest trading partner of India in the 
GCC followed by Saudi Arabia, Kuwait, Qatar, Bahrain and Oman. The GCC’s largest 
export to India appears to be oil, and main product groups imported from India 
to GCC are textiles, precious metals and food products. <br>
<br>
<b>Bilateral trade ties</b><br>
Indian visitors and Indian residents of the GCC constitute one of the largest 
groups of travellers between India and the Gulf. As per the data available, 
Indians have made substantial investments in the GCC. Expatriate Indians 
constitute a major business community with investments in trade, manufacturing, 
retailing and services. India too is a significant business and tourism 
destination for citizens of the GCC countries.<br>
<br>
India has a satisfactory track record in the GCC in terms of the contribution 
made by Indian industry. Indian companies have handled major construction 
projects – international airport at Abu Dhabi, crude/gas treatment plant in 
Zirku Island, UAE, transmission tower project – UAE, Urea Hydrolyser – Qatar, 
Gathering Centre – Kuwait, Salalah Power Project – Oman, and Formaldehyde 
Expansion Project, Al Jubail, Saudi Arabia.<br>
<br>
Indian companies continue to supply Critical equipment to the GCC countries as 
Evaporator Modules and Ammonia Converter Baskets (UAE); Waste Heat Recovery 
Boilers, Stripper Columns, Slug Catchers and Polypropylene Reactors (Saudi 
Arabia) – and are respected for the quality of the equipment. <br>
In addition, India has considerable experience in industrialisation and 
expertise to offer to GCC nations that are in the process of diversifying their 
industrial base to reduce dependence on the oil sector.<br>
<br>
<b>GCC initiative for economic progress</b><br>
GCC countries have also initiated measures for economic liberalisation. These 
include a move towards permitting foreigners to own business and properties. The 
Free Zones are attracting overseas companies to establish their bases in these 
countries, and Indian companies constitute the largest groups. The UAE has a 
lead in this regard over other GCC countries. <br>
<br>
This trend is expected to continue. The existing international political and 
economic environment is motivating the GCC countries to look at India for 
investments, for which there are excellent opportunities. Earlier, destinations 
in the West were favoured. This is a noticeable change that augurs well for 
strengthening the economic and industrial cooperation between GCC countries and 
India.<br>
<br>
The economic indicators for the region are encouraging. Total GDP of the region 
stands at about $321 billion (RO123.46 billion). The regional average per capita 
is about $10,380 (RO3992.31) at current prices with $8,000 (RO3,076.92) in Saudi 
Arabia, $8,200 (RO3,153.85) in Oman and $21,000 (RO8,076.92) in Qatar. The World 
Bank classifies the region as High-Income Group category. The region witnessed 
an average growth rate of six per cent per annum, at current prices from the 
1991 to 2000. <br>
<br>
GCC countries continue to rely extensively on hydrocarbons for their revenues. 
The region contains almost 45 per cent of the world’s proven crude oil reserves, 
accounts for 20 per cent of the world’s oil production and 23 per cent of 
world’s total exports.<br>
<br>
The realisation of the European common market, emergence of North American Free 
Trade Agreement, far reaching changes in the former USSR, and the economic 
resurgence in the Asian continent, especially in India and China, have impacted 
the GCC countries. Such initiatives are not limited to developed countries 
alone. Damascus Declaration for the greater Arab free market has been signed and 
is being implemented ahead of original schedule allowing free trading among 
member states by the end of 2005.<br>
<br>
GCC countries have accepted globalisation with an open mind. Annual tariff 
reduction, which started in 1998, is being carried out so that full trade 
liberalisation for goods of Arab origin can be achieved by end-2005. For 
centuries the region has been active in international trade and commerce and all 
but one GCC countries are members of WTO. GCC countries have witnessed 
significant developments in terms of adoption of Joint Agricultural Policy, 
Unified Industrial Development Strategy, the general framework of the GCC 
population strategy and the long-term comprehensive development strategy (2000 
to 2025). <br>
<br>
The unified economic agreement stresses the establishment of GCC joint projects 
in industry, agriculture and services using public, private or mixed capital in 
order to achieve economic integration with productive interface and common 
development on sound economic basis. It also encourages the private sector to 
set up joint projects linking the economic interest in all fields. Within this 
framework, a number of joint projects in industry, insurance, airline services, 
animal and fisheries have been set up.<br>
<br>
Trade integration is a priority. GCC Countries are unifying their industrial 
regulations and legislations to facilitate distribution of industries all over 
the GCC by encouraging establishment of basic projects according to the 
comparative economic advantage. To fulfil this objective, GCC countries have 
ratified the unified industrial strategy. The unified economic agreement has 
exempted all industrial products of GCC origin from Customs Duties and other 
such charges.</font></p>
