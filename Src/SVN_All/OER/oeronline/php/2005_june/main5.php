The GCC-India Industrial Conference, held in February 2004 at Mumbai, apart from 
being the first-ever formal economic dialogue between GCC and India, was the 
beginning of the new rationale of building economic ties. The theme of the 
Conference—Opportunities and Challenges in the 21st Century—captured the new 
spirit. Its focus was on four priority areas: trade, investments, industrial 
cooperation including SMEs, and transfer of technology including information 
technology.<br>
