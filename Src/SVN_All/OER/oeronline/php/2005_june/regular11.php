            <p align="justify"><font face="Verdana" size="4" color="#FF0000">
            Management<br>
            <br>
            </font><font face="Verdana" size="1"><b>Revenge of the Right Brain<br>
            </b><br>
            <b>Logical and precise, left-brain thinking gave us the Information 
            Age. Now comes the Conceptual Age - ruled by artistry, empathy, and 
            emotion. By Daniel H. Pink</b></font></p>
            <p align="center">
            <img border="0" src="manag.jpg" width="250" height="102"></p>
            <p align="justify"><font face="Verdana" size="3"><b>W</b></font><font face="Verdana" size="1">hen 
            I was a kid - growing up in a middle-class family, in the middle of 
            America, in the middle of the 1970s - parents dished out a familiar 
            plate of advice to their children: Get good grades, go to college, 
            and pursue a profession that offers a decent standard of living and 
            perhaps a dollop of prestige. If you were good at math and science, 
            become a doctor. If you were better at English and history, become a 
            lawyer. If blood grossed you out and your verbal skills needed work, 
            become an accountant. Later, as computers appeared on desktops and 
            CEOs on magazine covers, the youngsters who were really good at math 
            and science chose high tech, while others flocked to business 
            school, thinking that success was spelled MBA.<br>
            <br>
            Tax attorneys. Radiologists. Financial analysts. Software engineers. 
            Management guru Peter Drucker gave this cadre of professionals an 
            enduring, if somewhat wonky, name: knowledge workers. These are, he 
            wrote, ‘‘people who get paid for putting to work what one learns in 
            school rather than for their physical strength or manual skill.’’ 
            What distinguished members of this group and enabled them to reap 
            society’s greatest rewards was their ‘‘ability to acquire and to 
            apply theoretical and analytic knowledge.’’ And any of us could join 
            their ranks. All we had to do was study hard and play by the rules 
            of the meritocratic regime. That was the path to professional 
            success and personal fulfilment. <br>
            <br>
            But a funny thing happened while we were pressing our noses to the 
            grindstone: The world changed. The future no longer belongs to 
            people who can reason with computer-like logic, speed, and 
            precision. It belongs to a different kind of person with a different 
            kind of mind. Today—amid the uncertainties of an economy that has 
            gone from boom to bust to blah—there’s a metaphor that explains 
            what’s going on. And it's right inside our heads.<br>
            Scientists have long known that a neurological Mason-Dixon line 
            cleaves our brains into two regions – the left and right 
            hemispheres. But in the last 10 years, thanks in part to advances in 
            functional magnetic resonance imaging, researchers have begun to 
            identify more precisely how the two sides divide responsibilities. 
            The left hemisphere handles sequence, literalness, and analysis. The 
            right hemisphere, meanwhile, takes care of context, emotional 
            expression, and synthesis. Of course, the human brain, with its 100 
            billion cells forging 1 quadrillion connections, is breathtakingly 
            complex. The two hemispheres work in concert, and we enlist both 
            sides for nearly everything we do. But the structure of our brains 
            can help explain the contours of our times.<br>
            <br>
            Until recently, the abilities that led to success in school, work, 
            and business were characteristic of the left hemisphere. They were 
            the sorts of linear, logical, analytical talents measured by SATs 
            and deployed by CPAs. Today, those capabilities are still necessary. 
            But they’re no longer sufficient. In a world upended by outsourcing, 
            deluged with data, and choked with choices, the abilities that 
            matter most are now closer in spirit to the specialties of the right 
            hemisphere – artistry, empathy, seeing the big picture, and pursuing 
            the transcendent.<br>
            <br>
            Beneath the nervous clatter of our half-completed decade stirs a 
            slow but seismic shift. The Information Age we all prepared for is 
            ending. Rising in its place is what I call the Conceptual Age, an 
            era in which mastery of abilities that we’ve often overlooked and 
            undervalued marks the fault line between who gets ahead and who 
            falls behind.<br>
            To some of you, this shift—from an economy built on the logical, 
            sequential abilities of the Information Age to an economy built on 
            the inventive, empathic abilities of the Conceptual Age—sounds 
            delightful. ‘‘You had me at hello!’’ I can hear the painters and 
            nurses exulting. But to others, this sounds like a crock. ‘‘Prove 
            it!’’ I hear the programmers and lawyers demanding. <br>
            <br>
            OK. To convince you, I’ll explain the reasons for this shift, using 
            the mechanistic language of cause and effect. <br>
            <br>
            The effect: the scales tilting in favour of right brain-style 
            thinking. The causes: Asia, automation, and abundance.<br>
            <br>
            <b>Asia</b><br>
            Few issues today spark more controversy than outsourcing. Those 
            squadrons of white-collar workers in India, the Philippines, and 
            China are scaring the bejesus out of software jockeys across North 
            America and Europe. According to Forrester Research, one in nine 
            jobs in the US information technology industry will move overseas by 
            2010. And it’s not just tech work. Visit India’s office parks and 
            you’ll see chartered accountants preparing American tax returns, 
            lawyers researching American lawsuits, and radiologists reading CAT 
            scans for US hospitals. <br>
            <br>
            The reality behind the alarm is this: Outsourcing to Asia is 
            overhyped in the short term, but underhyped in the long term. We’re 
            not all going to lose our jobs tomorrow. (The total number of jobs 
            lost to offshoring so far represents less than one per cent of the 
            US labour force.) But as the cost of communicating with the other 
            side of the globe falls essentially to zero, as India becomes (by 
            2010) the country with the most English speakers in the world, and 
            as developing nations continue to mint millions of extremely capable 
            knowledge workers, the professional lives of people in the West will 
            change dramatically. If number crunching, chart reading, and code 
            writing can be done for a lot less overseas and delivered to clients 
            instantly via fibre-optic cable, that’s where the work will go.<br>
            <br>
            But these gusts of comparative advantage are blowing away only 
            certain kinds of white-collar jobs – those that can be reduced to a 
            set of rules, routines, and instructions. That’s why narrow 
            left-brain work such as basic computer coding, accounting, legal 
            research, and financial analysis is migrating across the oceans. But 
            that’s also why plenty of opportunities remain for people and 
            companies doing less routine work – programmers who can design 
            entire systems, accountants who serve as life planners, and bankers 
            expert less in the intricacies of Excel than in the art of the deal. 
            Now that foreigners can do left-brain work cheaper, we in the US 
            must do right-brain work better. <br>
            <br>
            <b>Automation</b><br>
            Last century, machines proved they could replace human muscle. This 
            century, technologies are proving they can outperform human left 
            brains – they can execute sequential, reductive, computational work 
            better, faster, and more accurately than even those with the highest 
            IQs. (Just ask chess grandmaster Garry Kasparov.)<br>
            Consider jobs in financial services. Stockbrokers who merely execute 
            transactions are history. Online trading services and market makers 
            do such work far more efficiently. The brokers who survived have 
            morphed from routine order-takers to less easily replicated 
            advisers, who can understand a client’s broader financial objectives 
            and even the client’s emotions and dreams.<br>
            <br>
            Or take lawyers. Dozens of inexpensive information and advice 
            services are reshaping law practice. At CompleteCase.com, you can 
            get an uncontested divorce for $249, less than a 10th of the cost of 
            a divorce lawyer. Meanwhile, the Web is cracking the information 
            monopoly that has long been the source of many lawyers’ high incomes 
            and professional mystique. Go to USlegalforms.com and you can 
            download—for the price of two movie tickets—fill-in-the-blank wills, 
            contracts, and articles of incorporation that used to reside 
            exclusively on lawyers’ hard drives. Instead of hiring a lawyer for 
            10 hours to craft a contract, consumers can fill out the form 
            themselves and hire a lawyer for one hour to look it over. 
            Consequently, legal abilities that can’t be digitised—convincing a 
            jury or understanding the subtleties of a negotiation—become more 
            valuable.<br>
            <br>
            Even computer programmers may feel the pinch. ‘‘In the old days,’’ 
            legendary computer scientist Vernor Vinge has said, ‘‘anybody with 
            even routine skills could get a job as a programmer. That isn’t true 
            anymore. The routine functions are increasingly being turned over to 
            machines.’’ The result: As the scut work gets offloaded, engineers 
            will have to master different aptitudes, relying more on creativity 
            than competence.<br>
            <br>
            Any job that can be reduced to a set of rules is at risk. If a 
            $500-a-month accountant in India doesn’t swipe your accounting job, 
            TurboTax will. Now that computers can emulate left-hemisphere 
            skills, we’ll have to rely ever more on our right hemispheres.<br>
            <br>
            <b>Abundance</b><br>
            Our left brains have made us rich. Powered by armies of Drucker’s 
            knowledge workers, the information economy has produced a standard 
            of living that would have been unfathomable in our grandparents’ 
            youth. Their lives were defined by scarcity. Ours are shaped by 
            abundance. Want evidence? Spend five minutes at Best Buy. Or look in 
            your garage. Owning a car used to be a grand American aspiration. 
            Today, there are more automobiles in the US than there are licensed 
            drivers – which means that, on average, everybody who can drive has 
            a car of their own. And if your garage is also piled with excess 
            consumer goods, you’re not alone. Self-storage—a business devoted to 
            housing our extra crap—is now a $17-billion annual industry in the 
            US, nearly double Hollywood’s yearly box office take.<br>
            <br>
            But abundance has produced an ironic result. The Information Age has 
            unleashed a prosperity that in turn places a premium on less 
            rational sensibilities – beauty, spirituality, emotion. For 
            companies and entrepreneurs, it’s no longer enough to create a 
            product, a service, or an experience that’s reasonably priced and 
            adequately functional. In an age of abundance, consumers demand 
            something more. Check out your bathroom. If you’re like a few 
            million Americans, you’ve got a Michael Graves toilet brush or a 
            Karim Rashid trash can that you bought at Target. Try explaining a 
            designer garbage pail to the left side of your brain! Or consider
            <br>
            <br>
            illumination. Electric lighting was rare a century ago, but now it’s 
            commonplace. Yet in the US, candles are a $2-billion a year business 
            – for reasons that stretch beyond the logical need for luminosity to 
            a prosperous country’s more inchoate desire for pleasure and 
            transcendence.<br>
            <br>
            Liberated by this prosperity but not fulfilled by it, more people 
            are searching for meaning. From the mainstream embrace of such 
            once-exotic practices as yoga and meditation to the rise of 
            spirituality in the workplace to the influence of evangelism in pop 
            culture and politics, the quest for meaning and purpose has become 
            an integral part of everyday life. And that will only intensify as 
            the first children of abundance, the baby boomers, realise that they 
            have more of their lives behind them than ahead. In both business 
            and personal life, now that our left-brain needs have largely been 
            sated, our right-brain yearnings will demand to be fed.<br>
            <br>
            As the forces of Asia, automation, and abundance strengthen and 
            accelerate, the curtain is rising on a new era, the Conceptual Age. 
            If the Industrial Age was built on people’s backs, and the 
            Information Age on people’s left hemispheres, the Conceptual Age is 
            being built on people’s right hemispheres. We’ve progressed from a 
            society of farmers to a society of factory workers to a society of 
            knowledge workers. And now we’re progressing yet again – to a 
            society of creators and empathisers, pattern recognisers, and 
            meaning makers.<br>
            <br>
            But let me be clear: The future is not some Manichaean landscape in 
            which individuals are either left-brained and extinct or 
            right-brained and ecstatic – a land in which millionaire yoga 
            instructors drive BMWs and programmers scrub counters at 
            Chick-fil-A. Logical, linear, analytic thinking remains 
            indispensable. But it’s no longer enough. <br>
            <br>
            To flourish in this age, we’ll need to supplement our well-developed 
            high-tech abilities with aptitudes that are ‘‘high concept’’ and 
            ‘‘high touch.’’ High concept involves the ability to create artistic 
            and emotional beauty, to detect patterns and opportunities, to craft 
            a satisfying narrative, and to come up with inventions the world 
            didn’t know it was missing. High touch involves the capacity to 
            empathise, to understand the subtleties of human interaction, to 
            find joy in one’s self and to elicit it in others, and to stretch 
            beyond the quotidian in pursuit of purpose and meaning.<br>
            <br>
            Developing these high concept, high touch abilities won’t be easy 
            for everyone. For some, the prospect seems unattainable. Fear not 
            (or at least fear less). The sorts of abilities that now matter most 
            are fundamentally human attributes. After all, back on the savannah, 
            our caveperson ancestors weren’t plugging numbers into spreadsheets 
            or debugging code. But they were telling stories, demonstrating 
            empathy, and designing innovations. These abilities have always been 
            part of what it means to be human. It’s just that after a few 
            generations in the Information Age, many of our high concept, high 
            touch muscles have atrophied. The challenge is to work them back 
            into shape.<br>
            <br>
            Want to get ahead today? Forget what your parents told you. Instead, 
            do something foreigners can’t do cheaper. Something computers can’t 
            do faster. And something that fills one of the non-material, 
            transcendent desires of an abundant age. In other words, go right, 
            young man and woman, go right.<br>
            <br>
            Adapted from A Whole New Mind: Moving from the Information Age to 
            the Conceptual Age, copyright © by Daniel H. Pink, published in 
            March by Riverhead Books. Printed with permission of the publisher. 
            Contributing editor Daniel H. Pink (dp@danpink.com) wrote about 
            Gross National Happiness in issue 12.12.</font></p>
