A unique experience<br>
<br>
</font><b><font face="Verdana" size="1">Its organisers claim that the Sandal 
Camp is a five-day unique experience among the picturesque beauty of Oman, one 
that is sure to change the outlook of the participants for the better. Sudipta 
Dasgupta finds out the truth in that claim</font></b><br>
<font face="Verdana" size="4" color="#FF0000">
<br>
</font><b><font face="Verdana" size="3">“I</font></b><font face="Verdana" size="1"> 
was quite worried about being accepted in an organisation that I had just joined 
and was a total stranger to everyone around. It was a new environment, and to 
know the people at the top- and middle-level management would take me at least a 
year or two. I was confused and worried about how I would mingle with the 
others.”<br>
<br>
Those were the initial inhibitions of Muhanned Alasfoor, the regional manager 
for the Dakhiliya &amp; Dhahira regions of Omantel, when he joined the company last 
year. Luckily for him, the telecom giant was organising an offsite at around the 
time he was hired. “I joined the Sandal Camp, which was being organised to take 
newcomers and other levels of management in the company for a breathtaking 
experience. It was this camp that changed my views forever,” he remembers.<br>
<br>
“Through this camp, I was able to mingle with people coming from different walks 
of life, and with a constant change in groups, it helped me overcome barriers 
and obligated me to deal with new faces time and again. The experience was 
simply wonderful from a discovery point of view as well as fun. The things I 
learned at the camp have proved to be very useful in both my career as well as 
personal life,” he claims.<br>
<br>
“Being the Regional Manager, I now fully understand the importance of 
team-building, because you have to constantly rely on other people’s opinions 
and views, which can only come with a consolidated and collective 
decision-making. <br>
<br>
It has truly been a great experience and most of all helped me know my 
colleagues well.”<br>
<br>
<b>So what is Sandal Camp?</b><br>
The Sandal Camp is a five-day unique experience among the picturesque beauty of 
Oman at the Sharqiya Desert about 200 km from Muscat. The camp organisers 
conduct various activities – like treasure hunts, intellectual problem-solving 
activities, adventure games, test driving skills in sand, camp fires and many 
more which embed and enhance qualities like teamwork, collaboration, trust and 
support amongst the participants. <br>
<br>
The Camp is particularly designed to identify and help meet an individual’s and 
his/her organisational needs like the above with the help of a team of experts, 
human resource and communication professionals who guide them through an 
experience of a lifetime to achieve leadership, customer service, 
communications, commitment to achieve, project team building, risk taking, 
decision-making, planning, competition et al.<br>
<br>
Sandal Camp creates opportunities for people taking part in this activity, which 
help them to realise their inner strengths and discover hidden qualities that 
they might never have experienced before. It not only helps professionals to 
look at their work and life from a different perspective, but also creates 
confidence in them.<br>
<br>
In addition, there are personal and confidential feedbacks given to all the 
participants by trained professionals who are flown in from Dubai specifically 
for this purpose. This is done for the participants to know how they performed 
during the program so they may apply those learnings to their work environment.
<br>
<br>
<b>How it all began</b><br>
One of the key persons who developed this incredible program and who’s also a 
Human Resource expert is Marie Ouellet. Along with her, the credit for the 
Sandal Camp goes to Mohamed Issa—the brain and the desert wizard, as Marie calls 
him—Mohammed Bin Ali Al Wohaibi, executive president of Omantel who constantly 
supported the project and made it happen.<br>
The ideat behind the program was conceived a year ago. Says Marie, the program 
facilitator: “It started off with an outing with Mohammed Bin Ali Al Wohaibi in 
the desert. Mohamed Issa was the guiding force behind this trip, and when we 
finished, we looked at the terrain and told ourselves that this is a great 
foundation for a team-building, communication-type program.’’<br>
<br>
‘‘The (Omantel) president then asked Mohamed Issa and me to design a suitable 
program. We started the concept and developed the program within three to four 
months’ period. We did the first program with 20 Omantel managers. It was a big 
success,” she adds. The rest, as they say, is history.<br>
<br>
<b>The activities</b><br>
One of the greatest challenges faced in this Camp is the Desert Crossing, where 
the participants go on a journey of 120 km with the objective of reaching a 
beach, with the final 20 km comprising of the most rugged dunes in the Middle 
East with no existing roads or tracks – the Sandal Dunes. But all of this is 
taken care with adequate food, water and safety equipments.<br>
<br>
On the way to the beach, there are camps that have been specially built with 
conference rooms, individual rooms with bathrooms and beds. So, people get a 
feel of the exquisiteness of nature with the beautiful surroundings, clean air 
and desert sand dunes.<br>
When asked as to what are the problem areas these programs try to solve, Marie 
answered, “The purpose of this program is to encourage teamwork. So, basically, 
if a team from a company is about to embark on a new project and the management 
wants this group to work together, such a group can be sent to the Sandal Camp. 
It will infuse a new bond and team spirit required to start off any project.”
<br>
<br>
Since the camp is held in the desert, there are a few risks involved, but the 
activities keep the participants quite busy and it is more of an adventure 
program done in a controlled environment and all the possible problems are taken 
care of. The camp is thus not focused on performance alone, but the fun factor 
is present too. The greatest challenge here is to attend the entire program and 
utilising the qualities gained at workplaces and at personal levels, which is 
the most vital and imperative idea of conducting such camps.<br>
<br>
<b>Take-home value</b><br>
“Sandal Camp is not there to teach participants, but rather to present an 
opportunity for them to learn. What they do with this opportunity will, in turn, 
determine the level of success they come away with after the five days are over. 
Sandal Camp is about presenting opportunities to create positive changes,” 
explains Mohammed Bin Ali Al Wohaibi.<br>
<br>
The Camp has been conducting such team building programs for professionals and 
has been successful in achieving an overall change of personalities in most of 
their clients. “After the five-day program, one of my favourite moments is to 
sit down and listen to the participants comments after the big adventure. They 
all change quite a lot; even the most quiet and discrete ones become talkative 
and expressive. Everyone has a story to tell, feelings to share, people to 
thank,” beams Marie. <br>
<br>
The camp’s clients include heavyweights like Omantel, Salalah Port Services, 
Bank Muscat besides many other medium-sized companies. The camp organises 
team-building programs in places most suitable to the clients and according to 
the needs of the clients. The Al Sawadi Resort is the next venue for the 
team-building program.</font></p>

