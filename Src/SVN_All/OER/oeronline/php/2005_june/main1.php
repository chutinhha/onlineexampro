<p align="justify" dir="ltr"><b><font face="Verdana" style="font-size: 16pt">
Green Oman, Clean Oman</font></b><font face="Verdana" size="4" color="#FF0000"><br>
</font><br>
<font face="Verdana" size="1"><b>Sayyida Tania the president of Environment 
Society of Oman, talks to OER’s Rafiah AlTalei about the newest NGO in Oman. 
Excerpts from an exclusive interview:<br>
</b>
<br>
</font><font face="Verdana" style="font-size: 12pt"><b>E</b></font><font face="Verdana" size="1">nvironment 
Society of Oman (<a target="_blank" href="http://www.environment.org.om"><span style="text-decoration: none"><font color="#000000">www.environment.org.om</font></span></a>), 
one of the newest NGOs in the Sultanate, was established by a small group of 
people who care and are concerned about environmental issues in the country. 
‘‘The Society was founded in March 2004 by a group of Omanis who have an 
interest in conserving the environment,’’ says Her Highness Sayyida Tania bint 
Shabib Al Said, the president of the Environment Society of Oman. <br>
<br>
They started work six years ago with their whales and dolphin group. That was 
where the idea of the environment society took root. HH Sayyida Tania said that 
the society had ‘‘plans for several activities, but our first priority right now 
is to rent an office in a good location, because now we have some money after 
the fundraising we did in last April. Right now, the Society’s members are 
working from home, and we are not operating from one place.’’ <br>
<br>
‘‘Second priority,’’ HH Sayyida Tania added, ‘‘is to have relations and 
establish projects to educate people throughout Oman with the association of 
ministries such as the Ministry of Education and Ministry of Municipalities and 
Environment. Our recent activity is a PowerPoint project for schoolchildren that 
teaches them what they can do for the environment while in school and at home. 
We also had a cleaning camp in Wadi AlAnsab, which is very close to the Seeb 
Airport. We spent more than three hours in clearing the site of construction 
waste.’’ <br>
<br>
‘‘Oman is a very clean country, and it is a tourism destination as well. So, it 
is very important to keep every part of the Sultanate clean,’’ she said. ‘‘In 
the Society, we also focus on environmental research - now we are considering 
research on whales and dolphins, so in a way, we continue to work on our old 
project as well,’’ she added.<br>
‘‘We in the Environment Society of Oman believe that the issues and challenges 
should be tackled from both sides (the government as well as civil society 
organisations). Just one of these can’t accomplish the job alone,’’ she 
explained. ‘‘One of our aims is to have members from all wilayats of Oman. It is 
very important to have a representative for all of them as we need to work and 
cooperate with each other,’’ she emphasised.<br>
<br>
<b>The aims of the Environment Society of Oman</b></font></p>
<p align="justify" dir="ltr"><font face="Verdana" size="1">The Environment 
Society of Oman is a non-governmental society, which aims to conserve Oman’s 
natural heritage and raise awareness about environmental issues. Its aims 
include:</font></p>
