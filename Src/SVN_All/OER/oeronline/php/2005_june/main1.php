  <li dir="ltr">
  <p align="justify" dir="ltr"><font face="Verdana" size="1">To engage in 
  conservation, education, and scientific pursuits in the public interest for 
  the purpose of conserving Oman’s wildlife and environment.</font></p></li>
  <li dir="ltr">
  <p align="justify" dir="ltr"><font face="Verdana" size="1">To raise public 
  awareness of (Issues concerning) Oman’s wildlife and <br>
  environment.</font></p></li>
  <li dir="ltr">
  <p align="justify" dir="ltr"><font face="Verdana" size="1">To enable Omanis to 
  take an active interest in and to volunteer in projects to <br>
  conserve Oman’s environment and wildlife.</font></p></li>
  <li dir="ltr">
  <p align="justify" dir="ltr"><font face="Verdana" size="1">To provide a forum 
  for the exchange of ideas and information related to environment and wildlife 
  in Oman.</font></p></li>
  <li dir="ltr">
  <p align="justify" dir="ltr"><font face="Verdana" size="1">To cooperate with 
  the appropriate local, regional, and international scientific organisations on 
  educational, conservation and scientific issues related to Oman’s environment 
  and wildlife.</font></p></li>
  <li dir="ltr">
  <p align="justify" dir="ltr"><font face="Verdana" size="1">To support the work 
  of government ministries and institutions by sharing information, drawing 
  attention to issues of concern and, when requested, being available for 
  consultation.</font></p></li>
  <li dir="ltr">
  <p align="justify" dir="ltr"><font face="Verdana" size="1">To develop 
  proposals for scientific research and, when appropriate, also be involved in 
  this research.</font></p></li>
</ul>
<p align="justify" dir="ltr"><font face="Verdana" size="1"><b>Current projects 
of Environment Society of Oman</b><br>
<b>1. B.Y.O.B. (Bring your own bag) – ‘‘Say NO to plastic’’ campaign:</b> A 
public awareness campaign aimed at educating people of the dangers of plastic 
bags and the harm they can cause to our wildlife, how they end up as litter and 
the harm they cause to the environment in general. The campaign aims to get 
people to bring their own bags or reuse old bags – all with the final goal of 
reducing the using of plastic bags. At present, such bags are being sold in some 
Al Fair outlets.<br>
<br>
<b>2. Mooring buoys:</b> Sport diving is becoming increasingly popular in Oman, 
with tourists coming from far and wide to see Oman’s rich ad unique marine life. 
One of the reasons they travel so far to dive here is that the marine 
environment is relatively unexplored and mostly in good condition. Diving can 
put pressure on the marine life that tourists come to see. In other parts of he 
world, such as the Red Sea, coral reefs have suffered extensive damage form 
diving activities, especially form boats that anchor on the fragile corals. Some 
coral colonies can take hundreds of years to grow and may be fatally damaged in 
just a few careless seconds by a heavy boat anchor. Installing mooring buoys at 
popular dive sites reduces the need for anchoring and so helps to minimise the 
impact from diving. It is also far more convenient to moor a dive boat than it 
is to haul a heavy anchor.<br>
<br>
Sayyida Tania explained: ‘‘The basic message is really very simple: By using 
mooring buoys, divers are protecting the marine life that they come to see and 
helping to preserve it for future divers to enjoy. We have manufactured two 
buoys and are in talks with various government bodies that will enable these to 
be placed in test locations.’’<br>
<br>
