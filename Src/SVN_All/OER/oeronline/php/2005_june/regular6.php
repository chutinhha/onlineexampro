<p align="justify" dir="ltr"><font face="Verdana" size="1">Employee Stock Option 
Plan (ESOP) is a plan through which a company awards Stock Options to the 
employees based on their performance. An ESOP gives the employee the right to 
buy a certain number of shares of your employer’s stock at a stated price over a 
certain period of time. Nearly all stock option plans are offered by companies 
that are publicly traded. However, even if your business is privately held, you 
can still use stock options. You may intend to sell your shares to the public 
eventually. If you intend to remain a privately-held company, employees can sell 
their exercised shares to you at a price determined to be a fair market value 
for your shares. <br>
<br>
The objective of ESOP is to motivate the employees to perform better and improve 
shareholders’ value. Stock options are issued, or granted, to key employees as 
call options. A call option gives the option holder the right (but not the 
obligation) to buy shares of the underlying stock at an agreed upon price for an 
agreed length of time. When employees decide to convert their options to shares, 
they exercise the options. The price at which they exercise is the exercise 
price or strike price. If the current share price is higher than the exercise 
price, the employee earns a profit. Assume for example that an executive is 
granted 100 options that can be exercised for 100 shares each. That gives the 
executive the chance to hold 10,000 shares if all options are exercised. If the 
exercise price is RO2 and the share price in the market is RO1, the options are 
said to be out-of-the-money by RO1 a share. If the share price rises to RO2.5, 
the options are in-the-money by RO0.500 a share. <br>
<br>
In general, stock options require employees to vest before they are able to 
exercise. Once vested, the employee generally retains the right to exercise for 
at least a couple of years. Options are generally issued at a price that is 
above the current share price, or out-of-the-money to encourage employees to 
work hard to increase shareholder wealth and consequently share price. Designing 
and implementing a stock option plan implicates many complex financial issues. A 
company stock option plan must set forth:<br>
• The number of shares that may be bought; <br>
• Type of stock option; <br>
• The exercise price; <br>
• The duration of the option; <br>
• Permitted form(s) of payment; and <br>
• Any limitation on stock purchase or transfer. <br>
<br>
Other variations of ESOP are ESPP (Employee Stock Purchase Plans), Compensation 
Plans, Incentive Plans, SAR/Phantom ESOPs etc. <b>ESPP </b>is generally used in 
listed companies, wherein the employees are given the right to acquire shares of 
the company immediately, not at a future date as in ESOP, at a price lower than 
the prevailing market price. Shares issued by listed companies under ESPP will 
be subject to lock-in-period; as a result, the employee cannot sell the shares 
and/or the employee has to continue with the employer for a certain number of 
years. <b>SAR/ Phantom Shares:</b> Under this scheme, no shares are offered or 
allotted to the employee. The employee is given the appreciation in the value of 
shares between two specified dates as an incentive or performance bonus, that is 
linked to the performance of the company as a whole, as reflected in its share 
value.<br>
