  <li dir="ltr">
  <p align="justify" dir="ltr"><font face="Verdana" size="1">whoever 
  counterfeits a mark registered in accordance with the law, or imitates it in a 
  manner tending to mislead the public, and whoever in bad faith uses a 
  counterfeit or imitated mark; or</font></p></li>
  <li dir="ltr">
  <p align="justify" dir="ltr"><font face="Verdana" size="1">whoever knowingly 
  sells, or offers for sale or dealing, or possesses with the intention of sale, 
  products having on them a counterfeit or imitated mark or one placed thereon 
  without rightful cause; or</font></p></li>
  <li dir="ltr">
  <p align="justify" dir="ltr"><font face="Verdana" size="1">whoever in bad 
  faith uses a mark identical or similar to a well-known mark, registered or 
  unregistered, to an extent which invites confusion [in the marketplace] </font>
  </p></li>
</ul>
<p align="justify" dir="ltr"><font face="Verdana" size="1">shall be punished by 
imprisonment for a period not exceeding two years and by a fine not in excess of 
two thousand RO, or by either of these two penalties. It is apparent that 
selling counterfeit products bearing the trademarks of major software 
manufacturers would qualify under each of these categories as a violation of the 
Trademark Law. <br>
<br>
Finally, the CPL provides in Article 21 that anyone violating the provisions of 
the CPL shall be punished with a fine of up to RO5,000 unless a more severe 
penalty is provided for under any other law. <br>
<br>
<b>So what is the problem?</b><br>
Legislation alone is not enough. Despite the considerable protection Omani 
legislation provides to intellectual property, software piracy continues to be a 
problem, both locally and within the region. Although stepping up the 
enforcement of these laws within the marketplace would help to some degree, one 
of the primary problems facing legal proceedings against pirates is the 
presentation of evidence to the court, as generally only the Ministry of 
Heritage and Culture will have access to the evidence obtained against pirates. 
Furthermore, local enforcement is only a temporary solution. A more reasonable 
approach would be for Gulf nations to work within the international community to 
apply pressure to the countries responsible for producing these counterfeit 
products, as stopping them at their source may be the only practical long-term 
solution.<br>
