Knowledge Oasis Muscat in Oman recently hosted its first conference on 
electronic gaming (&quot;e-gaming&quot;) designed to provide a forum for programmers, 
technology companies, and any other interested parties to discuss the future of 
entertainment software development in the Middle East. Although the conference 
discussions covered a broad spectrum of information technology related matters, 
one recurring issue was that of piracy and copyright enforcement in the Middle 
East.<br>
<br>
One major console producing company had estimated that platform developers had 
lost an incredible $460 million (RO177 million) due to piracy in the Middle 
East. Another speaker displayed 11 pirated software titles that had been 
purchased in Ruwi for a grand total of Rials Omani 16 (approximately $41.00). 
Had the games been purchased legitimately for their retail prices, the total 
amount for the software would have been RO184 (approximately $480.00).<br>
<br>
These examples beg the question: what protections are provided to intellectual 
property rights by legislation in Oman? The answer is that there are many 
protections provided, especially since Oman's accession into the Trade Related 
aspects of Intellectual Property Rights (&quot;TRIPS&quot;) agreement. <br>
<br>
<b>The Law for the Protection of Authors' Rights</b><br>
Sultani decree 37/2000 promulgating the Law for the Protection of Authors' 
Rights (&quot;Copyright Law&quot;) offers significant copyright protections to the authors 
of intellectual properties. The Copyright Law provides that authors of original 
literary, scientific, artistic, and cultural works shall enjoy the protection of 
the law, whatever the value of such works, whatever their type, whatever the 
means of expression employed, or the purpose for which they were created. 
Article 2 of the Copyright Law specifically provides that computer programmes 
and audio-visual works shall be protected under the law. The Copyright Law 
defines a computer programme as a compilation of terms and instructions 
expressed by any language, symbol or sign made for use in a computer whether in 
a direct or indirect manner, with the object of arriving at a specified result. 
This definition includes all computer software.<br>
<br>
The Copyright Law further provides that the rights of the author of intellectual 
properties shall include moral rights (to prevent changes, alterations or 
deletions to the work without his approval), and pecuniary rights (the 
reproduction of the work by any means whatsoever, and all forms of material 
exploitation of the work, including copies of such work).<br>
<br>
<b>The Law of Trademarks</b><br>
Equally significant in the context of piracy is Sultani Decree 38/2000 
promulgated the Oman Trademark Law (&quot;Trademark Law&quot;) protecting businesses from 
trademark infringements and unfair business practices. The Trademark Law clearly 
defines a trademark as anything that takes a distinctive, visually perceptible 
form, so as to show that it appertains to the owner of the mark by reason of the 
manufacture, selection, trading in or offering for sale of a product. This 
includes company symbols, certain words or phrases, or anything else that would 
associate a product or work with a particular manufacturer or trader.<br>
<br>
<b>The Consumer Protection Law</b><br>
Sultani decree 81/2002 promulgating the Consumer Protection Law (&quot;CPL&quot;) is 
slightly more recent in comparison to the preceding laws. As the name suggests, 
the law was promulgated in order to provide marketplace protection to consumers. 
Article 8 of the CPL states that consumers are entitled to receive accurate 
information about the product they intend to buy, including information relating 
to the manufacturer, place of manufacture, and after sales service. Furthermore, 
Article 16 requires a supplier to provide the consumer with truthful information 
regarding the quality and price of the product. Such suppliers are prohibited 
from making misleading or false statements.<br>
<br>
<b>Infringements</b><br>
Article 23(b) of the Copyright Law categorises as a copyright infringement the 
counterfeiting in the Sultanate of Oman of a work published abroad, or the 
placing of such work into circulation, or any other means of exploiting the work 
with the knowledge that such work is counterfeit. Additionally, the removal or 
degrading of any technical protection regulating public access to the work is 
also categorised as a violation of the law. It is reasonable to infer that the 
hacking or cracking of a software title’s copyright protection would be 
considered as a violation of the Copyright Law. Furthermore, Article 23(a) 
provides a catchall clause stating that any sale or placing into circulation in 
any manner whatsoever of a counterfeit copy of a protected work would constitute 
an infringement of the law.<br>
<br>
The Trademark Law prohibits any commercial acts which are meant to create 
confusion with a particular business, merchandise, or service in the 
marketplace. Furthermore in conducting any commercial activity, using 
information that causes the public to be misled as to the distinctive 
characteristics of merchandise is also prohibited. This is particularly 
significant as most pirated software bear counterfeit trademarks and other 
indicators found in the legitimate products in order to induce consumer 
confusion in the marketplace. <br>
<br>
Finally, by displaying a counterfeit product that bears the information and 
indicators of the legitimate product, a supplier is for practical purposes 
making false statements with respect to the quality and manufacturing origin of 
his product, thereby violating the provisions of the CPL.<br>
<br>
<b>Penalties</b><br>
The Copyright Law provides that whoever infringes any of the moral or pecuniary 
rights of an author shall be punished by imprisonment for a period not exceeding 
two years, and/or by a fine not exceeding two thousand riyals Omani. 
Significantly, the penalty shall be doubled if the offence is repeated. 
Furthermore, the competent court can order the confiscation of not only the 
infringing materials, but also of any tools used in creating the materials (such 
as a computer and its software).<br>
<br>
The Trademark Law provides in Article 35 that:</font></p>
