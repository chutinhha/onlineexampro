            sedans are quite ordinary in Oman. But when you get a basic sedan 
            with the features of a high-end vehicle, then there’s something 
            interesting. That’s what the all-new 2006 Ford Focus is about, which 
            was launched in Oman early last month by the Saud Bahwan Group. The 
            new Ford Focus has been given a makeover, though the legendary 
            automobile company has made sure they have not gone over the top. 
            Stylised, spacious and packed with top-class features, the Ford 
            Focus is a car that almost anybody will take to right away.<br>
            <br>
            The earlier version of the Ford Focus was introduced in Oman in 1999 
            and has had a small yet loyal fan following. The new 2006 Ford Focus 
            has been launched with the innovations after taking into 
            consideration the concerns and suggestions of the Ford loyalists. 
            The new Focus has four variants but only two have been launched in 
            the Sultanate — Ambiente and Trend. The Ford Focus’ Sport will be 
            launched later this year. <br>
            The car we took for a spin was the Focus Trend, which surprisingly 
            reminds you of the Mondeo from the same automotive company. Zippy 
            and loaded with comfort driving characteristics, the Focus makes for 
            a good driving experience. Aimed at the young and young at heart, 
            the Focus is tough yet easy to handle – a trait synonymous of all 
            vehicles from Ford.<br>
            <br>
            <b>The outside story</b><br>
            The Focus looks bigger compared to the older version, which is the 
            result of smart engineering. The new version has a wider rear track 
            as well as a longer wheelbase, which gives the Focus the feel of 
            being a big vehicle. On the road, the Focus demands attention with 
            its sleek and classy body. And it sure does get all the attention!<br>
            <br>
            The Focus has the largest wheel base in its class—16&quot; alloy 
            wheels—which aids in perfect road control and smooth driving even at 
            sharp curves. The windshield glass of the Focus is laminated, a new 
            feature in the revamped version and the side mirrors are automatic 
            as well as power heated. There are models in the same category that 
            come with power sunroof and are a good option if you are one for 
            cruising on the smooth roads of Muscat.<br>
            Another interesting and commendable feature of the Focus is 
            adjustable headlamps. The High-Intensity Discharge (HID) halogen 
            headlamps can be automatically adjusted depending on the surface the 
            car is on. While on the topic of headlamps, you just cannot miss the 
            improved ‘new-edge’ design that’s eye-catching and truly befitting 
            the Focus. The sedan also has new tail lamps; a welcome feature 
            especially when you are stuck in dimly lit surroundings. <br>
            <br>
            Power central locking with remote is another feature of the Focus.
            <br>
            <br>
            <b>The inside story</b><br>
            Innovations are more in the interiors of the car than on the 
            exterior. To begin with, the car is roomier with high roofline for 
            headspace, more leg space and comfortable seating. The seat can be 
            adjusted to suit the comfort of the driver and even at a normal 
            height, you just cannot help feeling as if you are sitting on a 
            pedestal inside the car. <br>
            <br>
            Anti-submarine seats in the Focus are not only comfy but also 
            integral to the safety feature of the car. Ford has a total of 11 
            interior colours, each includes two combinations to choose from in 
            Focus. As for the upholstery, the choice is between leather and 
            cloth.<br>
            <br>
            The dashboard has been restyled giving it a contemporary and mod 
            feel. Aesthetically placed, the controls are not just pleasing to 
            the eye but also positioned to suit the driver as well as the front 
            passenger’s convenience. One of the new additions in the sedan is 
            the cassette as well as in-dash 6 CD player, depending on the model 
            you choose. Ford has upgraded the audio amplifier system by stepping 
            up the number of speakers from four to six. Now that’s what I call 
            surround sound!<br>
            <br>
            Storage space has been increased too inside the Focus. The centre 
            floor console accommodates two cup-holders, front and rear stowage 
            bins and also a penholder. The overhead console is apt for stashing 
            away your sunglasses. The front doors have more storage space with 
            molded-in cup and bottle holders.<br>
            <br>
            I did not have to switch on the overhead light to search for the 
            petro card inside the glove compartment as it’s auto-illuminated, 
            yet another addition in the Focus. While on the subject of lights, 
            the control for the headlamps and sidelights are placed just below 
            the left hand A/c vent, near the driver, which is an innovative 
            feature for the Focus. There are visor vanity mirrors for the driver 
            as well as the front passenger, a feature usually seen in high-end 
            vehicles. That’s a brownie point for the Focus as far as most lady 
            drivers are concerned.<br>
            <br>
            <b>Safety factor</b><br>
            Ford knows it best when it comes to safety. The safety features of 
            the Focus include six airbags, including for the driver and front 
            passenger, front safety belt pretensioners, collapsible steering 
            wheel and advanced braking systems (ABS). This includes the standard 
            ABS, Electronic Brakeforce Distribution that would be handy during 
            sudden maneuvers and Emergency Brake Assist. A passive anti-theft 
            system is also part of the new sedan from Ford. The body shell of 
            the car is ultra-stiff and has a ring of protective steel to provide 
            better crash protection.</font></p>
