<p align="justify" dir="ltr" style="margin-left: 5; margin-right: 5"><b>
<span style="font-size: 14pt"><br>
</span></b><font face="Verdana" size="4">A Role Model <br>
</font><b><font face="Verdana" size="1">Legal eagle Majid Al Toky is among a 
select group of Omanis who have done the nation proud — by becoming the Resident 
Managing Partner of international law firm Trowers &amp; Hamlins. By Ramesh Kumar &amp; 
Zuhair Al Arabi</font></b><font face="Verdana" size="4"><br>
<br>
</font><font face="Verdana" size="1">
<img border="0" src="pic41.jpg" align="right" width="100" height="127">Majid Al Toky has reasons to feel like 
being on cloud nine. He is the first Omani to head the Oman office of the 
renowned firm of solicitors, Trowers &amp; Hamlins, as its Resident Managing 
Partner. He joined the firm eight years ago as a partner heading its litigation 
department. Despite his elevation to the topmost slot, the bespectacled, legal 
eagle remains extremely affable. Friendly to the true sense of the word, he 
speaks in hushed tones. That too, in different tongues: Arabic, English, French 
and Swahili!<br>
<br>
“An Omani heading an international law firm is something unique. I am proud that 
I did it,” says he without batting an eyelid. His is a good role model for many 
young Omanis who have aspirations to become lawyers and leaders in the business 
community.<br>
<br>
Having spent his early school life in French school in Africa, Al Toky convinced 
his parents that he could rewrite not only his future, but his entire family’s 
as well. Reluctant parents packed off the skinny Omani child to Egypt in the 
year 1974, which was proved the turning point. “I still recollect the bumpy ride 
on the Gulf Air flight to Cairo,” recollects the new pasha at Trowers &amp; Hamlins’ 
smart office on the Qurum beachfront. <br>
<br>
A philanthropic acquaintance, close to the family, came to his rescue in Cairo. 
He provided shelter to the young knowledge-seeker. On the one hand, the child 
had tears flowing because of separation from the family and on the other, there 
was a trace of happiness that he had made it: on way to pursue a serious 
educational journey, but with a “desire to return to my homeland and serve”. 
Gritting his teeth, Al Toky got immersed in his studies, overcoming 
homesickness, at least for the first two years.<br>
<br>
Soon after schooling, Al Toky chose to study law. Again in Egypt. Why law? “No, 
it was not the trend. I thought legal practice would be an ideal career. I am 
glad I was right. I have reached this far.”<br>
<br>
Back home, getting a job posed no difficulty. He joined Petroleum Development 
Oman (PDO). “Working for PDO was the most prestigious thing then — as it is even 
now,” points out the new chief of Trowers &amp; Hamlins. It was an era when PDO was 
deep into inking joint ventures and concession agreements with a slew of 
domestic and foreign companies. Al Toky literally burnt the midnight oil as the 
Legal Advisor of PDO, providing all sorts of legal advice, including advice on 
concession agreements, contracts, claims made by contractors, Tender Board 
issues and employment matters. This experience came handy when he participated 
in the drafting of the contract for the first Build-Operate-Transfer (BOT) 
project awarded by PDO for the construction of permanent accommodation for 
contractors’ staff working in oilfields in the interiors of Oman. <br>
<br>
Legal practitioners invariably cherish ‘courtroom dramas’, but Toky did not 
enjoy that luxury. He was more of an in-house lawyer, scanning minutely the 
tender documents that were to be presented to the Tender Board of PDO. “The idea 
is to avoid as many loopholes as possible in the interest of the company,” he 
adds. Impressed with his dedication and hunger for knowledge, PDO sent him 
abroad — to the United Kingdom — to obtain a postgraduate study in contracts and 
commercial law. He also spent a few months in the London office of Shell 
International, which helped him understand better the relationship between PDO 
and Shell and gain an international perspective. Having spent a decade with PDO, 
Al Toky became an authority on oil and gas business. What is more, he had 
developed an astute communication rapport with the Government at all levels.<br>
<br>
The vast experience acquired during this period and his commitment as a 
professional in his field made him an all rounded lawyer. During the 
negotiations of one of the contracts, he attracted the attention of the then 
Resident Managing Partner of Trowers &amp; Hamlins and the discussions on him 
joining the firm started immediately thereafter. “My friends and family thought 
it was crazy for me to let go of a prestigious position as the Legal Advisor of 
PDO for joining a law firm,” Al Toky says. He knew it was the right thing to do 
and he is glad that the partnership turned out to be a win-win for both. Says Al 
Toky: “The promotion proves to me that my partners have recognized my 
contribution.” When Al Toky joined the litigation department of Trowers &amp; 
Hamlins, there was only a team of two lawyers. The department has now grown to 
six lawyers, including two Omanis — Kamilia Al Busaidy and Abdullah Al Barwani. 
Adil Al Yahyai is currently on secondment to the Abu Dhabi office of Trowers &amp; 
Hamlins.<br>
<br>
Entry into the famed halls of Trowers &amp; Hamlins exposed him to a wide gamut of 
issues representing local and multinational clients on matters pertaining to 
construction contracts, agencies, insurance, shipping, corporate and commercial 
disputes. Business and disputes go together but for the young Al Toky, it was 
time to showcase his skills both as an arbitrator as well as a counsel in a 
number of high profile arbitration proceedings in Oman, London and in Singapore.<br>
<br>
With such an impeccable track record, invitations to be on the board and 
committees of high profile organisations at home and abroad were inevitable. No 
wonder then Al Toky is on the board of Capital Market Authority; he is the 
Chairman of Investment Working Group at Omani Centre for Investment Promotion 
and Export Development; he is a member of Oman LNG Forum and a board member of 
OPAL, a society formed by companies engaged in oil and gas activities in Oman. 
Besides, he advised on the formation of Nawras, the second mobile operator in 
Oman, where he was seconded for two years. Impressed by his performance, the 
Telecommunications Regulatory Authority in the United Arab Emirates roped in his 
services to advise on regulatory issues and disputes between operators there.<br>
<br>
He has traversed a long journey indeed, filled with hard work, persistence and 
eagerness to be recognized as a capable lawyer. Al Toky is committed to ensuring 
that Trowers &amp; Hamlins remains the leading law firm in Oman. With three children 
and also a keen interest in golf, he feels a fine balance between work and 
personal life is essential. His means of achieving this balance? Handle it all 
with a smile!</font><font face="Verdana" size="4"><br>
