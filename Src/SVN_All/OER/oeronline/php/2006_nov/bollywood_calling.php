<b><font face="Verdana" size="4">Bollywood Calling</font><font face="Verdana" size="1"><br>
</font>
</b><font face="Verdana" size="1"><br>
</font><b><font face="Verdana" size="1">Of late, there have been concerted 
efforts, led by OCIPED, to woo the movie moguls from Bollywood to come and shoot 
their films in Oman. While one can’t expect instant results, it’s definitely a 
step forward. Mehre Alam reports</font><font face="Verdana" size="2"><br>
<br>
</font></b><font face="Verdana" style="font-size: 11pt"><b>
<img border="0" src="bollywood.jpg" width="250" height="207" align="right" hspace="5" vspace="2">U</b></font><font face="Verdana" size="1">niquely Singapore. That’s the logo one finds on the promotional materials for 
the film Krrish, an Indian version of Superman, released across the world in 
June this year. But why ‘Uniquely Singapore’? One, the film — with some unique 
death-defying stunts — was shot largely in Singapore. Two, from the point of 
view of the small island-nation, it’s a cool marketing strategy. With the 
Bollywood flick taking the box office by storm (it was expected to), more and 
more Indian families are likely to pack their bags and fly off to Singapore this 
holiday season. It’s plain business logic.<br>
<br>
An Indian expatriate living in the Sultanate is planning something along similar 
lines. He plans to shoot his movie — to be released internationally — 
extensively in Oman. And he wants to use the slogan Magnificent Oman in all the 
publicity materials for his mainstream film.<br>
<br>
There are numerous examples of how shooting films can promote a country’s brand 
image. In recent times, the Lord of the Rings trilogy was shot extensively in 
New Zealand, and The Da Vinci Code in France. Both worked well for promoting the 
country brands.<br>
<br>
Aware of its huge potential to bring in huge revenues and business, many 
countries in Europe and elsewhere are rolling out the red carpet to woo 
filmmakers from distant shores, especially Bollywood. A Christchurch family 
business alone has worked with over 90 Indian film projects since launching its 
business in 1994. In the process, the family business, Kuran (NZ) Ltd., has 
developed a profitable niche in selling the Kiwi landscapes to Bollywood dream 
merchants.<br>
<br>
But when it comes to wooing Bollywood, New Zealand is facing some very stiff 
competition from countries like Switzerland, Australia and South Africa; they 
are all doling out incentives and banking on government-backed marketing drives 
to lure Indian crews.<br>
<br>
Coming back to Oman. The country has all that it takes to emerge as a prime 
destination for filmmakers — pristine locations, easy connectivity and friendly 
people. And for a filmmaker, it’s cost effective too. For example, the visa fee; 
Calculated in US dollars (See infographic), comes to US $23 in Oman against $76 
for Canada, $56 for Germany, $45 for Italy, $79 for Dubai, $146 for New Zealand, 
$86 for Australia and $100 for the US.<br>
<br>
Let’s take the average cost per crew on food and beverages. It comes to $5 in 
Oman, $19 in Canada, $38 in Germany, $100 in Mauritius, $75 in Italy, $18 in 
Dubai, $48 in Switzerland, $31 in New Zealand, $37 in Australia and $30 in the 
US.<br>
<br>
The cost of hiring a coach/50-seater bus for a day comes to $207-259 in Oman, 
$645 in Canada, $409 in Dubai, $503 in South Africa, $801 in Switzerland, $681 
in New Zealand, $623 in Australia and $850 in the US. (Source: <i><b>OCIPED</b></i>)<br>
<br>
<b>The OCIPED Initiative</b><br>
These are just a few examples of how Oman can be an economical choice for 
filmmakers, with all other advantages in place. Perhaps sensing the huge 
business potential, the OCIPED (Omani Centre For Investment Promotion &amp; Export 
Development) has taken the lead in enticing Bollywood. They have created a 
website dedicated to attracting Bollywood filmmakers.<br>
<br>
<img border="0" src="bollywood_calling_1.jpg" align="left" hspace="5" vspace="2" width="100" height="129">Says HH Faisal bin Turki Al-Said, Head of Planning and Development Department, 
OCIPED: “We thought it’s important to understand what India has to offer and 
what its film industry is looking for vis-a-vis Oman. So we appointed a 
freelancer to carry out a detailed study on this. He even met some reputed 
producers in India. Ramgopal Varma, the leading Indian producer-director who 
made the movie Naach, was initially looking to go to Mauritius, but decided to 
come to Oman.”<br>
<br>
That, indeed, was a leap forward for brand Oman. Varma shot some breathtaking 
song sequences for Naach in the backdrop of the Sultanate’s pristine beaches.<br>
<br>
It all started a few years ago when a leading Bollywood production company 
scouting for shooting locales approached the OCIPED to elicit information on the 
country. “We (OCIPED) realised the potential and the need to create a platform. 
That’s where the website initiative came from,” asserts HH Faisal. The website 
aims at providing film producers an opportunity to look into Oman’s potential — 
what it takes to come to Oman; and which are the supportive industries that can 
cater to the requirements <br>
of a film unit.<br>
<br>
“Since the launch of the website, four films have been shot in Oman, with an 
estimated US$200,000 spent here in the Sultanate. This illustrates the scope of 
opportunities available here,” adds HH Faisal.<br>
<br>
The OCIPED will gradually transfer the responsibility of maintaining the website 
to the Ministry of Tourism. “This (Ministry) will be the right umbrella,” points 
out HH Faisal, adding that the OCIPED has already briefed the Ministry about the 
huge benefits of inviting Bollywood to Oman. “We hope that the Ministry will be 
sending its own team to hold discussions with reputed producers and filmmakers 
of India,” he says.<br>
<br>
According to HH Faisal, Oman needs to be promoted in such a way as to realise 
its huge potential — the beaches, the landscape etc. “Some of the people we had 
invited here were absolutely amazed to see what this country has to offer. They 
were surprised they did not know much about it. This is where OCIPED’s role 
comes in — promoting Magnificent Oman. After that, it boils down to specifics. 
We complement the work of the Ministry of Tourism and the other ministries as 
well,” he says.<br>
<br>
<b>The Business of Films: Ripple Effect</b><br>
For countries vying to attract filmmakers, the potential is huge — tourism, 
trade, catering, transport and logistics, accommodation, direct employment and 
the showcasing of local culture and landscapes... are all part of the 
trickle-down effect.<br>
<br>
“Tourism creates jobs. The film industry is nothing but an offshoot of tourism. 
It’s a separate product offered by tourism. According to rough estimates, 
Bollywood industry has created more than one million jobs directly or 
indirectly,” says Rajeev Chowdhary, who plans to make the first Bollywood movie 
to be filmed entirely in Oman.<br>
<br>
He adds: “Once Bollywood starts arriving in big numbers, the process will create 
supporting industries — transport, lighting, sound systems, caravans, catering 
etc. All we offer in Oman now is locations, hotel rooms and transportation. The 
bouquet can be expanded, and this itself will create a lot of jobs. Ideally, we 
should be offering technicians, studios, specialists, animation and sound mixing 
specialists etc. That’s how the winning business formula can emerge.”<br>
<br>
In the past decade and a half, Bollywood has indeed gone global in terms of the 
locations they select. The trend has big benefits for the host countries: the 
locations turn into popular tourist destinations for Indian travellers.<br>
Switzerland was, perhaps, the first to benefit. Indian tourists made a beeline 
for the Alps, a popular Bollywood outdoor location. Following Switzerland’s 
example, countries like Thailand, Singapore, the UAE, South Africa, Austria, 
Canada and Egypt have upped their incentives to lure the Indian filmmakers.<br>
<br>
Singapore’s Tourism Board has gone on record to say that Bollywood is the best 
way to “showcase Singapore to Indians.” The country expects 700,000 visitors 
from India in the current year, compared to around 600,000 in 2005.<br>
The Board’s “Film in Singapore Scheme” has funded 10 projects so far. Under this 
$10 billion scheme, the Board subsides up to 50 per cent of the total cost 
incurred by an international film production company. It also provides logistics 
and ground support, and assists in getting the necessary approvals — including 
permission to block roads and traffic — from the respective government agencies.<br>
<br>
Shot in England, Scotland and Wales, blockbusters like Dilwale Dulhaniya Le 
Jayenge, Kuch Kuch Hota Hai and Mohabbatein, has made the British government 
travel that extra mile to lure Bollywood — obviously with the intention to 
promote, produce and distribute ‘Made in London’ movies. And to cash in on the 
long queues of tourists coming from India.<br>
<br>
Another fast emerging hotspot for Bollywood filmmakers is Leicester, an 
Asian-dominated city in England. The Yorkshire Tourist Board has already 
submitted a bid to the International Indian Film Academy Awards (IIFA), one of 
the biggest events of Bollywood, to host its 2007 awards <br>
in Yorkshire. Quick on the take, Eros International (a Bollywood distribution 
company) is reported to have listed its shares on the London Stock Exchange 
(LSE) and raised sizeable funds through equity placements.<br>
<br>
Says a film trade pundit: “Oman by itself cannot make or break a film by way of 
finance or technology. The film has to cover the other regions too, like the GCC 
countries or the pan-Arab countries. But anybody who makes a good commercial 
film will do good business. Films made in Kuwait and Egypt run in the theatres 
in Oman. This part of the world is awakening to the business potential of world 
cinema.”<br>
<br>
<b>UAE: Dream Merchants’ Dream</b><br>
The UAE, especially Dubai, has emerged as a hot favourite for Bollywood — to 
some extent even upstaging Hollywood. Since the mid-80s and 90s, Bollywood dream 
merchants have been courting Dubai with aplomb.<br>
<br>
This year, Dubai hosted IIFA Awards, one of the biggest events of Bollywood, 
with the who’s who of the Indian film industry rubbing shoulders with the UAE’s 
glitterati. Bollywood biggies performing stage shows has become a common feature 
in Dubai now. And Indian film crews filming songs with a backdrop of Shaikh 
Zayed Road or the Deira Corniche is almost routine business.<br>
<br>
Perhaps there’s more to it than meets the eye. It may have something to do with 
deep-rooted cultural and business ties as well. Indian cinema’s reigning 
superstar Shah Rukh Khan is reported to have purchased a prime property in the 
UAE, and most of the Indian filmstars are simply in love with Dubai. On the 
Hollywood front too, the developments are unfolding at a stunning pace. A 
Hollywood firm is reportedly planning to set up a US$1 billion film city in Ras 
Al Khaimah, a picturesque emirate in the UAE.<br>
<br>
Mirage Holdings FZCO, the US$1 billion firm, boasts of a crew that has won 
back-to-back Oscars for special effects in films like The Matrix and What Dreams 
May Come. The comprehensive project, according to recent media reports, is 
slated to open up the inflow of celebrity US film czars into the quiet environs 
of Ras Al Khaimah. It will be, probably, one of the largest film and 
post-production centres in the world in dollar terms.<br>
<br>
<b>Oman: Very Positive Feedback</b><br>
Hollywood and Bollywood both seem to have woken up to Oman’s potentials and 
advantages, following thumbs-up signal from those who visited the Sultanate 
recently. A Bollywood crew was in Oman a couple of months ago to shoot the film 
Fool And Final. Ahmed Khan, the film’s director, was thrilled to see the 
Sultanate’s locales. “It’s great to be here in Muscat. This place is like 
Mauritius. It reminds me of a beach in Melbourne. It has a ‘soft’ feel to it,” 
he is reported to have said.<br>
<br>
Something similar happened to Ramgopal Varma, better known in Bollywood for 
running a ‘movie factory’. The natural beauty of the Sultanate — the hidden 
caves in the Jissa/Qantab beach — had him smitten. <br>
<br>
Says Bob Tzudiker, an actor, writer and a notable TV artiste of the US: “Oman 
has assets that are difficult to reproduce elsewhere in the world. The 
infrastructure is excellent, with excellent road access to remarkable scenery, 
and easy four-wheel drive access to a vast area. It has locations that have not 
been seen, yet can be reached easily, and with the help of locals who are 
enthusiastic about showing their country to the world.” Tzudiker has acted in 
famous movies such as Total Recall, Moving Target and Omega Syndrome etc.<br>
<br>
A friend of Amin Ghani, the action director of the latest film shot in Oman, 
recalls how he was going ‘ga ga’ about the pristine locations of Oman. 
Filmmakers like Pahlaj Nihlani and Mukesh Bhatt (both big names in Bollywood) 
have also been approached to come to Oman and shoot their films here. And why 
not?<br>
<br>
Travelling to Shimla (an exotic Indian location in the Himalayas) from Mumbai 
costs more than travelling to Muscat from Mumbai! It makes perfect business 
sense for a Bollywood film crew to head for Magnificent Oman.<br>
However, while there is tremendous goodwill all around, the fact remains that 
Oman has a long way to go before being counted as a filmmaker’s preferred 
destination.<br>
<br>
<b>Oman: The Challenges</b><br>
Needless to say, challenges abound. One of the main challenges, says the 
director of a big corporate house in Oman, (they have facilitated the shooting 
of some Bollywood films in Oman) is that Oman, as a brand name for filmmakers, 
has not really picked up, like Thailand, Egypt, Malaysia, New Zealand or Dubai. 
Secondly, unlike the UAE, Oman as a whole has not been able to offer fantastic 
facilities to filmmakers, he says, and cites the example of Umm Al Quwain, one 
of the emirates of the UAE, which literally offers the entire emirate to 
Bollywood for shooting purposes. It obviously fetches handsome revenue for the 
emirate. The facilities offered in Dubai are much more attractive. The stars too 
prefer to be in Dubai any day compared to being in Muscat.<br>
<br>
Third, Oman may have a disadvantage when it comes to hotel rooms. When the crew 
come in they want to shoot outdoors. Now, outdoor shooting in Oman is possible 
in mainly four months — from mid-October to mid-March. And this is the prime 
season for the hotel industry as well, when occupancy rates hit really high, and 
thus, the space crunch. However, new hotels are coming up in the Sultanate and 
this problem can obviously be sorted out.<br>
<br>
Four, there is no real film industry in Oman. There are no real roots, like 
theatre, for the film industry, explains our source. Also, there is hardly any 
music industry in Oman. Now, music industry across the world acts as a platform 
that complements and supplements the film industry.<br>
<br>
What about the cinema theatres? The number is definitely not on the higher side 
in Oman. And it surely curtails the business of films, something growing 
worldwide. However, sources say a multiplex four-screen cinema is coming up in 
place of the Al Nasr theatre in Muscat, and a multiplex is coming up each in 
Sohar and Sur as well. <br>
<br>
Then, more important than anything else, the marketing of destination Oman 
perhaps needs to be done more aggressively. “If Hollywood can shoot films in 
India, Pakistan, Afghanistan, Morocco, Tunisia, and Dubai, why can’t they do it 
in Oman?” wonders an Omani film trade pundit. “We have similar landscapes; we 
are quite organised; we have relatively good facilities; movies that depict the 
Arab world can easily be shot here, the way it is done in Morocco.”<br>
<br>
But as the saying goes, Rome was not built in a day. Led by OCIPED, efforts are 
being made to attract Bollywood. But we must compete against countries that have 
set up multi billion dollar funds to help finance films; countries that are 
willing to lure filmmakers in with incentives that are far more attractive than 
those for conventional business. <br>
<br>
If one looks at investment in film compared to say conventional direct 
advertising of a country, it is easy to see the advantage of film, one can 
utilise the mobilized film crew to shoot sequences that can be used by the 
country’s tourism board, if the film is successful one can get a return on the 
money invested, the country name can feature in the film, products relating to 
the country can be placed in the film. The opportunities for subtle yet 
effective promotion are massive in any production.</font></p>

