            </font><font face="Verdana" size="5">No Backseat Driving</font></b><font face="Verdana" size="1"><br>
            <b>OER talks to George Ranjit Alexander, Divisional Manager-Consumer 
            Electronics Division, Omasco (part of Al-Futtaim Group) who believes 
            in setting and sharing goals instead of micro managing</b><br>
            <br>
            <img border="0" src="pic9.jpg" align="right" hspace="5" vspace="5" width="129" height="159">For a Keralite (from southern part of India) to dream about a career 
            in the Gulf is nothing unusual. In fact, the seeds of pitching one’s 
            own tent in the region germinates right from birth. Hailing from 
            Kollam (a small town in the southern state of India) Alexander, 
            scouting for a ‘lucrative job’ in Gulf is nothing unusual. Net 
            result: Oman became his first port of call and Oman Marketing &amp; 
            Services Co (Omasco), his maiden employer in the Sultanate.<br>
            <br>
            The 41-year old Piscean civil engineer has a MBA from the 
            prestigious University of Bombay. Surprisingly, the Omasco’s 
            Consumer Electronics boss — lording over a multi million dollar 
            business ably assisted by a 60 plus manpower in managing the 
            distribution of 78 product groups in Oman since February 2004 — 
            never felt a liking for construction discipline. If so, why study 
            civil engineering? “Parental compulsion,” pat comes the reply and he 
            hastens to add that he has a penchant for mathematics. “I knew my 
            heart was beating for marketing and so I headed for a MBA in 
            marketing,” adds he. <br>
            <br>
            When he came out successfully, finding a suitable job — marketing, 
            indeed — was not difficult. He joined personal luggage giant 
            Aristocrat Marketing in 1991 and rose from the ranks of management 
            trainee to Branch Manager by the time he left after a five-year 
            stint. The next few years saw Alexander marketing and managing 
            washing machines and other home appliances at a national Level for 
            blue chip Indian companies like Onida, IFB, BPL until the Gulf bug 
            had bitten him. <br>
            <br>
            “Omasco was the only company I had applied for in the Gulf and hit 
            on target in the maiden attempt,” reveals a congenial Omasco 
            Consumer Electronics’ chief seated at his Wattaya corporate office. 
            He chose Omasco to knock on purely because of his love to be in the 
            consumer electronic segment and the company’s girth or wide range of 
            products in this segment: volume may be puny compared to Indian 
            standards, but the large product line up and international exposure 
            enthused him.<br>
            <br>
            Does he miss India? Yes and no. “The Indian job was equally exciting 
            except that I have had little time for family as I was crisscrossing 
            the vast country setting up and running business for my employers. 
            Omasco, on the other hand, offers the thrill plus time to spend with 
            my family,” explains the graying Omasco’s Consumer Electronics’ 
            chief who immensely believes in “continuous and sustained growth”.<br>
            <br>
            <b>Core Philosophy</b><br>
            Like his namesake the great warrior, the Omasco honcho puts immense 
            faith in “aligning the employees’ vision with the company vision”. 
            This can happen, he points out, only if the leader is a votary of 
            delegation, trustworthy and approachable. “I am not a control freak, 
            which is not needed in this changing world, because everyone has 
            self respect and self motivated to achieve their own goals. Backseat 
            driving is not advisable under such circumstances. Setting and 
            sharing goals is my primary responsibility. Once delegated, I don’t 
            micro-manage by looking into day-to-day operations. If so, I am not 
            needed at all,” philosophises Alexander. Sense of humour is another 
            of Alexander’s speciality. He uses it occasionally to drive home the 
            point and create a relaxed atmosphere.<br>
            <br>
            <b>Market and Competition</b><br>
            Oman, given its size, does not actually tax Alexander. Managing 
            consumer electronics in Oman is not as complex as in India but gives 
            him a lot of exposure to retail management and consumer buying 
            behavior. “I don’t get bogged down by competition. I concentrate on 
            ensuring we remain the brand leader through constant innovation and 
            excellent after sales service,” surmises he. Planned growth strategy 
            is another strength for the man who sells consumer electronic wares 
            from the likes of Panasonic, General Electric, Black &amp; Decker, 
            Aftron, Xper, BPL etc. Under his stewardship, the overall growth has 
            been a consistent 15-20 per cent year after year and a stupendous 
            30-35 per cent spike in the retail segment alone. Truly, a 
            remarkable record. Ensuring a sustained increase in sales, improved 
            productivity and enhancing corporate image are the goals for this 
            god-fearing Syrian Christian from India. <br>
            <br>
            <b>Goals</b><br>
            “My personal goal is to be known as a person who came, built 
            business with a human touch and integrity,” avers he. On the 
            professional front, Alexander looks ahead to lead a company one day. 
            He is hungry for growth and firmly believes his ambition will be 
            answered given the changing economic profile of the Sultanate.<br>
