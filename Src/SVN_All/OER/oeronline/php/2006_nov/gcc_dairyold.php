        <img border="0" src="pic77.jpg" align="left" hspace="5" width="60" height="42">BAHRAIN</b><br>
        <b>Economic issues dominate polls </b><br>
        Economic issues are at the heart of debates among candidates for 
        Bahrain’s parliamentary and municipality elections slated for November 
        25. Tens of men and women are competing for 40 seats in each of 
        parliamentary and municipality councils. The subjects attracting debates 
        include jobs for locals, economic reforms and diversification, to name a 
        few.<br>
        <br>
        One such matter relates to declining representation of Bahraini 
        workforce in private sector jobs. Bahraini nationals accounted for a 
        mere 23 per cent of employment in private establishments in the first 
        quarter of 2006, thereby continuing a disturbing trend. Nationals 
        represented nearly 32 per cent of private sector jobs in 2001 only to 
        decline to 24 per cent in 2005. The issue is vital in the light of 
        greater involvement of the private sector in the economy, thanks to 
        on-going economic reforms that call for limiting public sector role. 
        Eventually, the trend could aggravate unemployment problem in the 
        country, which already stands at least 8 per cent among nationals.<br>
        <br>
        <img border="0" src="pic75.jpg" align="right" hspace="5" width="125" height="134">Still, 
        the process of economic reforms, notably privatisation, is a matter of 
        growing concern. So is the case as increasingly foreign firms assume 
        control of major public assets. This is meant to do away with 
        inefficiency in government-run establishments. Earlier in the year, 
        three foreign firms assumed control of Hidd power and water plant, the 
        primary utility complex in the country. Yet, recently, an international 
        firm won a 25-year contract to manage Bahrain’s ports services, namely 
        Mina Salman in Manama and Khalifa bin Salman in Hidd (which is under 
        construction). Thus, candidates expressed fears about job prospects, as 
        profit-seeking firms assume greater role in the economy. <br>
        <br>
        The third problem concerns poverty. The United Nations Development 
        Programme (UNDP) considers a Bahraini family, comprising six members, as 
        poor if monthly income falls below US$936. Many Bahraini families rely 
        on support extended by the Ministry of Social Development -- and more 
        importantly, on charitable associations scattered throughout the nation. 
        Relatively strong population growth of more than 2.5 per cent puts more 
        pressure on the poverty debacle in the Kingdom. A number of election 
        candidates have made improving living conditions of citizens a key poll 
        promise. The fourth economic concern deals with solving the housing 
        problem. Many nationals depend on government-subsidized facilities to 
        solve their housing problem. However, newspapers speak of more than 
        40,000 pending applications for various types of loans extended by 
        state-owned Housing Bank. Signifying the matters, Housing Bank recently 
        secured a commercial loan amounting to US$265 million as part of its 
        efforts to contain the problem. <br>
        <br>
        The fifth challenge relates to limited public sector spending. 
        Candidates argue that expanded oil receipt allows the authorities to 
        spend more rather than less. In fiscal 2005, the budget recorded a 
        surplus of US$680 million, thereby reversing planned deficit of US$778 
        million. Yet, this surplus was achieved partly due to the government 
        decision not to spend money allocated for capital projects. The 
        authorities consumed 53 per cent of allocated budget for capital 
        spending. Thus, candidates are urging the government to spend more in 
        the light of firm oil prices. <br>
        <br>
        Last but not least, there is the issue of increasing significance of oil 
        in the treasury income. Oil accounted for 76 per cent of total revenues 
        of 2005, up from 73 per cent in 2004 due to firmer oil prices in 
        international markets. Increasing reliance on oil runs contrary to the 
        government’s goal of diversifying the economy away from the hydrocarbons 
        sector. Like elsewhere, improving the wellbeing of people is the main 
        topic in Bahrain’s elections for candidates and voters alike.</font></p>
