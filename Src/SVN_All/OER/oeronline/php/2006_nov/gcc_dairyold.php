            <font face="Verdana" size="1"><b>GCC currencies undervalued against 
            US dollar</b><br>
            All the GCC currencies are undervalued against the US dollar, 
            reveals a study by Dubai Chamber of Commerce and Industry. No wonder 
            Kuwait Central Bank revalued its dinar against the US dollar. The 
            Bahraini Dinar is the most undervalued currency in the GCC 
            countries, and the UAE Dirham is the least undervalued currency. 
            Since 2000, the six GCC undervalued currencies have not undergone 
            any adjustment, neither upwards nor downwards, except the Kuwaiti 
            Dinar that has gone through several adjustments during this period. 
            This indicates that the Kuwaiti Dinar is the least stable GCC 
            currency during recent years, says the report. <br>
            <br>
            The study has used The Economist’s Big Mac Index, based on the 
            relative dollar price of a McDonald’s Big Mac burger in different 
            countries to the US price, to calculate the values of the GCC 
            currencies. For instance, in the UAE a Big Mac costs AED 10. 
            Dividing this by its US price of USD 3.10 produces a dollar PPP 
            (Purchasing Power Parity) against the dirham of AED 3.2258, compared 
            with the current exchange rate of AED 3.6725. This suggests that the 
            dirham is undervalued by 12 per cent. On the other hand, the Kuwaiti 
            Dinar and the Omani Riyal are undervalued by 16 per cent each, the 
            Qatari Riyal is undervalued by 20 per cent, the Saudi Riyal is 
            undervalued by 23 per cent, and the Bahraini Dinar is undervalued by 
            27 per cent.<br>
            <br>
            “A reasonably undervalued currency is good for non-oil re-exports 
            competitiveness, provided that it is not causing other serious 
            economic problems to the economy,” the report comments on whether 
            the currency should undergo revaluation.</font><div align="center">
