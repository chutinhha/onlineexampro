        companies and they have to retain 30 per cent of income in the Kingdom 
        to cover payouts. The 13 companies will have a total capital of US$698 
        million and they will float shares worth US$249.7 million for public 
        subscription. The capital of the companies ranges between US$26.6 
        million and US$213.3 million. One of the companies has agreed to float 
        47.49 per cent of its shares, nine others will float 40 per cent, and 
        the remaining three between 25 and 31 per cent of the shares.<br>
