        Cooperative Insurance, Arabian Shield Insurance, Sanad for Cooperative 
        Insurance, Saudi United Cooperative Insurance (Amity), Assurance Saudi 
        Fransi, Al-Ahlia Insurance, Mediterranean &amp; Gulf Insurance &amp; Reinsurance 
