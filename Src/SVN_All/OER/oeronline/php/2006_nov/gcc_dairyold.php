        <img border="0" src="pic78.jpg" align="left" hspace="5" width="58" height="40">KUWAIT</b><br>
        <b>Record budget surplus on cards</b><br>
        For the current fiscal (FY06/07), Kuwait is expected to post a record 
        budget surplus between US$18.66 billion to 25.92 billion, according to 
        an economic report prepared by National Bank of Kuwait (NBK). Revenue 
        estimates and a rise in oil prices contributed to Kuwait’s largest ever 
        budget surplus — US$23.85 billion for the last fiscal year (2005-06), 
        said the report.<br>
        <br>
        <img border="0" src="pic79.jpg" align="right" hspace="5" width="225" height="170">According 
        to the NBK report, the surplus for fiscal 2005-2006 was “the largest on 
        record and equivalent to 29 per cent of the 2005 gross domestic 
        product.” Recently released figures by the Ministry of Finance show 
        revenue growth increased to 53 per cent, and the fiscal surplus more 
        than doubling from a year ago. The surplus, at US$23.74 billion before 
        the mandatory allocation of 10 per cent of revenues to the Reserve Fund 
        for Future Generations (RFFG), was the largest on record.<br>
        <br>
        The surplus does not include investment income, which is not officially 
        reported by the government and is treated as an off-budget item. 
        According to balance of payments statistics, investment income from 
        abroad, which primarily accrues to the State’s foreign assets managed by 
        the Kuwait Investment Authority, was estimated at US$6.53 billion in 
        2005.<br>
        <br>
        The NBK report indicates that the government revenues reached US$47.46 
        billion on sharply higher oil revenues, which shot up to more than three 
        times budget projections and 59 per cent above the previous year’s 
        figure. Behind the rise in oil revenues was a 46 per cent increase in 
        the price of Kuwait crude, which averaged US$51 per barrel during the 
        fiscal year compared to US$35 the previous year.<br>
        <br>
        In contrast, non-oil revenues declined by 2.4 per cent on their previous 
        year level to US$2672 million, with their share of total revenues 
        falling to 5.6 per cent, less than half what it was only two years ago. 
        According to the report, total expenditures stood at US$23.71 billion in 
        2005-06, 9 per cent higher than the previous year. The biggest boost to 
        spending growth came from higher fuel costs incurred by the Ministry of 
        Energy for power generation. Fuel costs rose by 24 per cent representing 
        66 per cent of spending. <br>
        <br>
        <b>Profits boost in Q2</b><br>
        After a poor show in the first quarter, Kuwaiti companies recovered 
        during the second quarter to record the consolidated profits of US$3954 
        million in the first half of 2006. Second quarter profits of US$2504 
        million were up 5.9 per cent compared to the same period last year and 
        as much as 82 per cent higher than poor results in the first quarter. In 
        Q1 2006, as many as 26 companies reported losses, totaling US$591.1 
        million.<br>
        <br>
        According to NBK report, Q2 06 results were mixed with the industrial, 
        banking, and services sectors seeing their earnings grow by 30 per cent, 
        29 per cent and 21 per cent respectively, while the other sectors 
        witnessed declines in earnings. While the banking sector showed 
        consistently stable earnings growth over time, the investment sector was 
        particularly unstable. The most severe volatility in earnings growth 
        took place in Q4 05 and Q1 06 when earnings first increased 
        significantly before seeing a large drop. Furthermore, the earnings of 
        the banking sector appear to be largely uncorrelated with the rest of 
        the market, avoiding the ups and downs witnessed by other sectors, says 
        the report.<br>
        <br>
        The results show that the increases in earnings of the industrial and 
        services sectors can be attributed to only two companies, National 
        Industries Group Holding and Mobile Telecommunications Co. The banking 
        sector was the only one to see broad-based growth in earnings, with all 
        listed banks achieving positive growth in net profit, says the report.<br>
        Of the reporting companies, 71 saw an increase in their Q2 06 earnings 
        compared to a year before with their consolidated figure up by 47 per 
        cent. These companies’ consolidated earnings, which reached US$2256 
        million, represented 90 per cent of the total. Another 51 companies’ 
        profits drop by US$384.4 million, while 21 incurred losses totaling 
        US$60.15 million. These losses were well below losses suffered during Q1 
        06.</font></p>
