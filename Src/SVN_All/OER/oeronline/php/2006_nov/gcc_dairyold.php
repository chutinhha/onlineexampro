		<p align="justify"><font face="Verdana" style="font-size: 14pt"><br>
        </font><b><font style="font-size: 14pt" face="Verdana">Corporate 
        Governance: A Long Way to Go</font></b><font face="Verdana" size="1"><br>
        <b>The GCC countries need to pull up their socks in matters of corporate 
        governance, reports Sowmya Sri Rallabhandi citing the recent 
        IIF-Hawkamah survey report<br>
        </b><br>
        <img border="0" src="pic73.jpg" align="right" hspace="5" width="125" height="136">When it comes to corporate governance, the GCC countries lag 
        significantly vis-a-vis international corporate governance best 
        practices in the emerging markets. However, regional regulators are 
        pushing hard for reforms so as to lift corporate governance standards. 
        This is the find of the Institute of International Finance (IIF) and 
        Hawkamah — the Institute of Corporate Governance, which have recently 
        come out with the first-ever corporate governance report on GCC 
        countries: Corporate Governance in the GCC — An Investor Perspective.<br>
        <br>
        “The IIF-Hawkamah survey is part of a coordinated strategy towards the 
        harmonisation of corporate governance standards in the GCC and their 
        alignment with international best practices,” said Dr Nasser Al Saidi, 
        Executive Director of Hawkamah. The GCC average for compliance with 
        international best practices is nearly 50 per cent, considerably lower 
        than in other emerging markets — like India’s 75 per cent and about 65 
        per cent in China — cited the IIF.<br>
        <br>
        Oman, says the report, has the strongest corporate governance framework 
        — complying with about 70 per cent of IIF’s guidelines — followed by 
        Kuwait and Saudi Arabia, both with about 50 per cent compliance, and 
        Bahrain and the UAE with 40 per cent compliance of IIF guidelines. <br>
        <br>
        The IIF-Hawkamah survey found that Qatar had the weakest corporate 
        governance framework, and needs a great deal of improvement in its 
        corporate governance as it complies with only 35 per cent of the IIF 
        guidelines — earning the country an overall score of 2 out of 5. The 
        taskforce expects improvement, as the Doha authorities are taking steps 
        to improve governance practices in listed companies.<br>
        <br>
        “The findings of this survey will help GCC policy-makers identify key 
        areas for reform and promote awareness of the benefits good corporate 
        governance brings to companies and markets,” said Dubai International 
        Financial Centre (DIFC) governor Dr. Omar Bin Sulaiman.<br>
        <br>
        Hawkamah executive director Dr. Nasser Saidi said the survey showed that 
        corporate governance in the GCC was generally at an early stage of 
        development. The survey evaluated corporate governance standards across 
        some 56 criteria, falling into five broad categories:<br>
        <br>
        </font><font face="Times New Roman" size="1">&#9679; </font><font face="Verdana" size="1">Minority shareholders’ rights,<br>
        </font><font face="Times New Roman" size="1">&#9679; </font><font face="Verdana" size="1">Accounting and auditing standards,<br>
        </font><font face="Times New Roman" size="1">&#9679; </font><font face="Verdana" size="1">Responsibilities of board of directors,<br>
        </font><font face="Times New Roman" size="1">&#9679; </font><font face="Verdana" size="1">Laws governing ownership, and<br>
        </font><font face="Times New Roman" size="1">&#9679; </font><font face="Verdana" size="1">Laws governing control structure.<br>
        <br>
        Developments in corporate governance in the GCC have been largely driven 
        by four key factors, says the report. They are: <br>
        </font><font face="Times New Roman" size="1" color="#C0C0C0">&#9658;</font><font face="Verdana" size="1"><i>Capital market regulators are using the recent price correction in GCC 
        stock markets to ‘upgrade’ corporate governance frameworks.</i><br>
        <br>
        <img border="0" src="pic74.jpg" align="left" hspace="5" vspace="2" width="125" height="136">Corporate governance codes are being drafted and introduced by capital 
        market authorities in the GCC. Muscat and Abu Dhabi exchanges introduced 
        codes in 2003 and 2006 respectively, while regulators in the UAE, Saudi 
        Arabia, Bahrain, Qatar and Kuwait have drafted codes, expected to be 
        implemented in 2007.<br>
        <br>
        </font><font face="Times New Roman" size="1" color="#C0C0C0">&#9658;</font><font face="Verdana" size="1"><i>Increased corporate activity by GCC corporations in international 
        markets is contributing to improvements in private sector standards, in 
        line with international best practice.</i><br>
        <br>
        </font><font face="Times New Roman" size="1" color="#C0C0C0">&#9658;</font><font face="Verdana" size="1"><i>The banking sector in the GCC has made a significant contribution, 
        following undertakings by central banks to comply with Basel I and II 
        requirements.</i><br>
        <br>
        Central banks in all six GCC countries have amended their banking 
        regulations to include corporate governance-related requirements such as 
        establishing transparency and disclosure in financial statements, 
        establishment of a board level audit, nomination and compensation 
        committees and improved risk management.<br>
        <br>
        </font><font face="Times New Roman" size="1" color="#C0C0C0">&#9658;</font><font face="Verdana" size="1"><i>The opening of GCC stock markets to foreign investors is expected to 
        improve standards in GCC-listed companies, due to higher expectations 
        from these investors.</i><br>
        <br>
        A strong commitment to better corporate governance from the political 
        authorities as well as from senior government officials involved with 
        capital market development is needed for real change to take effect, the 
        study said.<br>
        <br>
        <img border="0" src="pic75.jpg" align="right" hspace="5" vspace="2" width="125" height="134">Referring to the public sector undertakings, the IIF-Hawkamah report 
        observes that regulators should quickly introduce corporate governance 
        reforms in state controlled companies, which constitute a majority of 
        companies in the GCC.<br>
        <br>
        The report calls for greater cooperation and coordination among 
        regulators in the region to strengthen equity markets in the GCC. With 
        the exception of Saudi Arabia and Kuwait, equity markets in the region 
        are relatively small and lack depth of listings. The IIF-Hawkamah 
        taskforce calls for the creation of a regional GCC corporate governance 
        task force comprised of regulators and market participants with the 
        mandate to harmonise capital market laws, promote cross listing of 
        stocks on exchanges across GCC with the objective of creating unified 
        GCC equity market.<br>
        <br>
        Dr. Saidi said reforms were being carried out across the region and 
        regulators were using the recent price correction in GCC stock markets 
        to upgrade corporate governance frameworks. Bahrain is revising its 
        company law and will shortly introduce a corporate governance code. 
        Saudi Arabia issued a draft corporate governance code in August on which 
        it has invited comments and will shortly enforce one for listed 
        companies. In Oman, the Muscat Securities Market is likely to be 
        privatised.<br>
        <br>
        To bring the GCC into compliance with the IIF’s corporate governance 
        code, the report suggests recommendations including:<br>
        <br>
        </font><font face="Times New Roman" size="1">&#9679; </font><font face="Verdana" size="1">Promote shareholder activism in the region by creating a class of 
        institutional investors.<br>
        </font><font face="Times New Roman" size="1">&#9679; </font><font face="Verdana" size="1">Improve governance related reporting by requiring companies to provide 
        information to shareholders in annual<br>
&nbsp; reports and on company websites.<br>
        </font><font face="Times New Roman" size="1">&#9679; </font><font face="Verdana" size="1">Establish specialised courts to deal with enforcement of securities 
        laws. Increase financial transparency <br>
&nbsp; by harmonizing financial reporting 
        requirements.<br>
        </font><font face="Times New Roman" size="1">&#9679; </font><font face="Verdana" size="1">Develop healthy accounting practices.<br>
        </font><font face="Times New Roman" size="1">&#9679; </font><font face="Verdana" size="1">Establish registrars of companies, requiring all companies (from sole 
        proprietorship to joint stock companies)<br>
&nbsp; to provide financial 
        information.</font></p>
