        44 per cent growth seen in the entire 2005 reported in a semi-annual 
        economic and financial publication recently released by the National 
        Commercial Bank (NCB). If the current earnings growth momentum were 
        extended over to the entire 2006, the combined net-profits of all the 
        listed companies should reach close to US$22.6 billion compared to 
        US$17.44 billion achieved in 2005. This will be the slowest earnings 
        growth since 2002 whereby the market recorded yearly average 
        profitability growth of nearly 50 per cent in the three years to 2005. 
        The slow growth is largely attributed to the 11 per cent drop in SABIC 
        earnings in the first half of this year, the report said.</font></p>
