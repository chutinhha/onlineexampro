<p align="justify" dir="ltr" style="margin-left: 5; margin-right: 5"><b><br>
</b><font face="Verdana" size="4">Brewing Up a Storm <br>
in a Café Cup</font><font face="Verdana" size="1"><br>
<b>I have spent my entire career building brands, says J. Bruce Elliot, 
president of Toronto-based international café chain Second Cup — on a recent 
‘check-out mission Middle East’ — in a freewheeling interview with Abhijit Sinha</b><br>
<br>
<i>
<img border="0" src="pic60.jpg" align="right" hspace="5" width="250" height="188">What’s your business style?</i><br>
I like to get into the market place, not sit around in my office. I’ve been to 
everyone of our café in Canada. We have been there for more than three decades 
and have 370 of them. I know most people by name.<br>
<br>
<i>What brings you to the Middle East?</i><br>
It’s a real growth market for us and very important for me to come to Oman. I’ve 
been getting some real substantive feedback from our operational people. We have 
a big partnership with Bin Hendi Enterprises, our master franchisee in Dubai. 
Qatar too is on my radar besides a joint venture in Kuwait City. Second Cup will 
be going to Egypt and Saudi Arabia. We signed up a group from Cairo and from 
Riyadh too while in Dubai.<br>
<br>
<i>So Second Cup is going places.</i><br>
From this trip, I find that Canada as a brand is viewed as a very positive thing 
by people across the world. So a Canadian brand is a good proposition. On top of 
it, the café stores are all locally owned with their respective marketing plans. 
They have their own ideas. For example, the owners here have insights of what’s 
the trend in Muscat. We can’t see this from Dubai, far less from Toronto. We 
respect that. That’s our model.<br>
<br>
<i>How did the name Second Cup come about?</i><br>
Back in 1975, two enterprising gentlemen got together in Canada and opened an 
independent café. They came up with this wonderful name. The coffee here is so 
good that you’ve got to have at least a Second Cup. So, one café grew to 
another, and soon there were quite a few. It’s gone through a number of 
different owners. Now Cara, the largest F&amp;B company in Canada, owns the brand — 
as part of their portfolio of several brands.<br>
<br>
Starting 2007, the Second Cup will be owned by the former CEO of Cara who has 
bought the company, Second Cup Ltd., after retirement. So, it remains one of the 
few 100 per cent Canadian owned brands, projecting Canada as a premium success 
story.<br>
<br>
<i>What’s the net worth of Second Cup?</i><br>
It’s a publicly traded company listed on the Toronto Stock Exchange, worth US$ 
200 million.<br>
<br>
<i>What next?</i><br>
We will start to do more things to make our brand come alive — and be more 
explicit in terms of its being Canadian. Whatever I’ve learnt from this trip, 
that’s an asset we will use.<br>
<br>
<i>What did you learn?</i><br>
My trip was more to meet face to face the owners of Second Cup. I did my 
planning in Toronto where we have a business school — kind of boot camp to train 
the local owners. I came to see them first hand. Now, I’ve a personal 
relationship with them. I’ve checked out all competitive stores — coffee shops. 
So now I can help them with their marketing plans.<br>
<br>
<i>What’s your volume growth in the MENA (Middle East and North Africa) sector?</i><br>
Well, we’re in the early days. We have been here only three years. The first was 
Dubai so we are 31 years old in Canada, and three years old in the Middle East. 
We have about 20 stores and cafes in the Middle East. We see significant growth 
— double…triple…quadruple number of cafes. This business is all about the 
intensity of presence. We need to have a number of cafes for the brand to grow.<br>
<br>
<i>Can you quantify?</i><br>
In the Middle East? Easy. We started with nothing. Now we are at 20. My senses 
say we’ll be double by next year, and would be adding 15-20 cafes a year. We 
have partners who have aspirations to grow. <br>
<br>
<i>What’s the next target?</i><br>
Egypt and Saudi Arabia are on track. Next comes Bahrain. The accelerator is down 
in Oman and Dubai. Kuwait is lovely. Beirut is obviously challenged today. We 
are getting our game together in the Middle East and South Asia. Lots of people 
are calling from India, China, Russia and Eastern Europe. So, we are getting 
focused. We want our partners to be successful. We are only successful if they 
are — for ours’ is a royalty model — and the only way we make money is if they 
do. It’s a very honourable relationship.<br>
<br>
<i>Are you keeping pace with other international players?</i><br>
We are not in the game of beating them in the number of café stores. We want 
good sites and locations, and good hosts. Not just good offers. We are not here 
to be in some race. Rather, we are trying to differentiate our brands — and one 
of the ways is to go for local ownership.<br>
<br>
<i>How much freedom do you give to the local owners to go their own way in terms 
of décor etc?</i><br>
We have standards. People come to Second Cup with certain expectations. But we 
let the local owners to interact and sponsor community activities — like the 
owners here have a lot to do with promoting art. In fact, they are personalities 
in their own rights; they are very well known.<br>
<br>
As for interiors, colour, design etc, it’s all local, but within parameters of 
course, because we want uniqueness; likewise for some foodstuff — bakes and 
sandwiches. So long as it delivers the premium, and is within the range of the 
spirit of the arrangement we’ve got. We can’t have things that detract from the 
value and equity of Second Cup.<br>
<br>
<i>Has it been a two-way learning trip?</i><br>
Yes. I was at the Mall of the Emirates and felt that perhaps we have missed the 
boat — in terms of being local. So I’m going back to a design firm and say we 
should be using a different colour palette. Another lesson was how important 
Canada is as a brand. Now, we have just a flag in the Second Cup café stores. 
But we could do a lot in a very premium way. That’s the association we are 
looking for with our franchisees.<br>
<br>
<i>Do you subscribe to any management guru?</i><br>
I love Michael Porter. He has crystal clear strategies and un-relentless 
execution. Both are inseparable. Lots of MBAs and companies write strategies but 
do not execute; and then go back and rewrite the strategies. That’s a 
no-brainer.<br>
<br>
<i>What’s your strategy?</i><br>
Differentiate your own brand! Responsible growth! And spending lots of time on 
people you are investing in, particularly the managers running the stores. 
That’s my bundle. It’s best to be consumer relevant.<br>
<br>
<i>Are you a workaholic?</i><br>
No! But I was so earlier — back in the beer business. It’s difficult to get off 
in a big corporate. But I learnt. Now, I work hard and play hard. Ice hockey is 
my game; I play as a centre-right. I lost my spleen. My wife wants me to stop as 
I play an aggressive game. Summers are fly-fishing time in eastern Canada. Best 
Atlantic salmon — Labrador, New Finland, Quebec… I also run a lot and have a gym 
at home to stay in shape. I am a big believer in the maxim: Sound mind in a 
sound body. I love hanging out with my young daughter. I like to spend time with 
my family.<br>
<br>
<i>How do you see your own growth?</i><br>
I was running a US$ 3.5 billion company. I’ve held 11 senior positions on the 
way up — started off in the beer sales business and spent most of my career in 
marketing. Now I’m running a US$ 200 million one. Here, we don’t let the 
pressure of share price dictate our strategies. We have a lot of corporate 
responsibility, but not the pressure to grow from quarter to quarter. That’s 
where people start making short-term decisions.<br>
<br>
Our whole game is to be successful cafe by café, and country by country. We are 
in for a long haul. The last thing we want is to set up someone who fails with a 
premium brand. That’s why we are very careful in choosing our partners — and 
having once chosen, very responsible. Our major competitors are all 
corporate-owned. Our local franchises have got their own money in this business 
— and it’s a very different, as the saying goes, when you’ve staked your own 
skin in the game.<br>
<br>
So, the biggest decision for us is, who will be our partner. And when you’ve got 
great partners, guess what… You’ve got to be successful!</font></p>

