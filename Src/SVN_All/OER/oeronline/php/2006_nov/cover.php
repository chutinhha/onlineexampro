            <p align="justify" style="margin-left: 10px; margin-right: 10px">
            <font face="Verdana" size="1"><b>Growth Hindrances</b><br>
            Industry sources admit that lack of a specialised training college 
            is an important problem, which is of importance in view of high 
            level of Omanisation in insurance sector. Leaders of the industry 
            suggest formation of a training institute like the College of 
            Banking and Financial Studies. The institute can be a joint 
            initiative of the government and private sector. <br>
            <br>
            The insurance industry worldwide has undergone remarkable structural 
            changes in recent years while facing even greater exposure to market 
            uncertainty. Ineffective market discipline is one of the issues 
            identified by the Association of Insurance Regulator as a threat to 
            the emerging markets. This is an issue that has to be effectively 
            tackled for the healthy growth of the insurance market. While the 
            regulator has a role to play in ensuring market discipline, the role 
            played by the well-informed public in bringing about discipline in 
            the market place cannot be ignored as the key to effective market 
            discipline lies in public disclosure and consumer education.</font></p>
