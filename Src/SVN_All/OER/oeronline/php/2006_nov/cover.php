            <font face="Verdana" size="1">Also, there is a need for medical insurance products in developing 
            countries like Oman as the cost of medical expenses has been 
            increasing year after year. In many countries, the government 
            agencies are gradually withdrawing from the healthcare sector due to 
            mounting cost and the responsibility is being gradually taken up by 
            insurers world over and the same phenomenon is observed in the Gulf 
            region.<br>
            <br>
            National Life — the largest life insurance firm in the Sultanate — 
            consider medical insurance products as a growth area. In line with 
            the strategy, the company has launched various products to suit 
            different requirements of the people. Apart from inpatient and 
            outpatient coverage in Oman, National Life offers treatment in 
            Indian sub-continent and Europe. The company also made it convenient 
            for patients by tying up with 25 clinics across the country. With 
            this, the insured employees of companies can choose from several 
            clinics spread across the country. <br>
            <br>
            As expected, liberalization of this sector has helped bring about 
            several positive developments, including use of innovative channels 
            for distribution. Bankassurance — marketing of insurance products by 
            commercial banks — is also picking up in recent years. This helps 
            the insurance companies to market their products effectively in 
            remote areas where banks have branch operation. Oman International 
            Bank (OIB) has tied up with Muscat Insurance for marketing the 
            latter’s product.<br>
            <br>
            Insurance-linked auto-finance is another opportunity for local 
            companies to tap. Last year, United Finance Company and Oman United 
            Insurance Company have jointly launched an insurance-linked auto 
            finance scheme. Both companies have joined together in an agency 
            partnership agreement, which has an impact on the personal lines 
            insurance products are marketed in the country. The insurance policy 
            built into the loan provides comprehensive coverage and it covers 
            the full tenure of the loan. With the insurance built into the loan, 
            the customer does not have the hassle of making separate insurance 
            payment. </font></p>
