            <p align="justify" style="margin-left: 10px; margin-right: 10px">
            <font face="Verdana" size="1"><b>Motor Insurance</b><br>
            Motor insurance segment constitutes the single largest business of 
            local companies. As far as the national companies are concerned, 
            motor insurance constitutes around 47 per cent of total premium 
            income in 2005 as against 52 per cent in the previous year. In the 
            case of foreign companies, motor segment constituted 44 per cent of 
            total premium income in 2005 as against 38 per cent in the previous 
            year, said CMA in its report on Insurance Market Review. </font></p>
            <p align="justify" style="margin-left: 10px; margin-right: 10px">
            <font face="Verdana" size="1">But the claims-to-premium ratio of 
            motor insurance is relatively high when compared to other countries. 
            If all companies apply good underwriting practices and penalise poor 
            driving, there will be less accidents on Oman’s roads. In a way, 
            better drivers subsidise poor drivers who in many cases enjoy 
            similar rating, which is not in line with good underwriting 
            practices.<br>
            <br>
            <b>Major Players</b><br>
            The country’s insurance market is mainly dominated by five national 
            companies — Dhofar Insurance, Al Ahlia Insurance, Muscat Insurance, 
            Oman United Insurance Company and National Life. With an employee 
            strength of 741 people, the national companies generated a combined 
            insurance premium income of RO92.3 million in 2005. Of this, RO80.29 
            million was from general insurance segment.<br>
            <br>
            Of late, life insurance companies are focusing on medical insurance 
            as a growth area in the coming years. The segment is poised for 
            rapid growth in the region as GCC governments are gradually 
            withdrawing from healthcare sector. In Oman alone, the premium from 
            the segment is expected to touch RO25 million with in a period of 
            two to three years. Expatriates are encouraged to go to private 
            clinics and hospitals, if the case is not an emergency, which 
            created a big market for both clinics and insurance companies. As 
            fast industrialization is taking place at Sohar, the demand for 
            medical insurance products is expected to pick up in the coming 
            years. </font></p>
