            <font face="Verdana" size="1">Industry sources said that after the 
            fall down of ONIC Insurance, there was a vacuum in the general 
            insurance segment. The regulators have brought in new stipulations 
            after the collapse, which was blamed for undercutting and poor 
            reinsurance practices. In fact, the liquidation of ONIC Insurance 
            prompted CMA to bring in several stringent norms to check unhealthy 
            practices in an apparent move to strengthen the financial position 
            of existing players. Accordingly, the insurance regulator has 
            brought in a new code of corporate governance, besides prohibiting 
            consultancy and direct insurance broking of brokers with reinsurance 
            broking services for the same client. Also, the regulating authority 
            is in an advanced stage of announcing important amendments to 
            Insurance Companies Law in conformity with international standards 
            and principles.<br>
            <br>
            With more firms entering the field, existing players are 
            strengthening their capital base. For instance, the total paid up 
            capital of national insurance companies shot up by 35.5 per cent to 
            RO49.8 million in 2005 from RO36.75 million for the previous year. 
            This growth is attributed to raising of capital by Oman United 
            Insurance Company (OUIC), Dhofar Insurance Company, Royal &amp; Sun 
            Alliance Insurance and National Life. OUIC is further raising its 
            capital from RO8 million to RO10 million by floating a rights issue, 
            as strong capital base is required for surviving in a liberalized 
            environment. With a premium of 100 basias, the share is offered at 
            200 basias. The additional capital will help the company to compete 
            with international firms. </font></p>
