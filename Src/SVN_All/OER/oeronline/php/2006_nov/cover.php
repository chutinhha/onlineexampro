                  <p align="justify">
            <font face="Verdana" size="1"><b>CHALLENGES BEFORE GLOBAL INSURANCE 
                  INDUSTRY</b></font></p>
                  <p align="justify">
            <font face="Verdana" size="1">The global insurance industry 
                  generates about US$3 trillion in premium income accounting for 
                  more than 8 per cent of global GDP. The industry has weathered 
                  much turbulence in the last five years. Terrorism attack 
                  (9/11), natural catastrophes (Katrina and Tsunami) and major 
                  accidents have severely affected the bottomlines of many 
                  insurance companies. Some have been wiped out completely. 
                  Simultaneously the industry faced the challenge of adjusting 
                  to a new financial markets paradigm characterized by low 
                  fixed-income yields and less vibrant stock markets. The 
