            <p align="justify" style="margin-left: 10px; margin-right: 10px">
            <font face="Verdana" size="1"><b>Booming Market</b><br>
            It is not far to seek the reasons for the recent enthusiasm. The low 
            insurance penetration in the country, a vacuum created in the 
            insurance sector following the collapse of ONIC Insurance, better 
            growth in gross domestic product, the recent boom in gas-based 
            industries and the peculiar demographic feature are all cited as 
            positive factors for a better demand for insurance products. Direct 
            insurance premium income (of both life and non-life insurance) rose 
            by 14.9 per cent to touch RO117.27 million in 2005, over the 
            previous year, according to data released by the Capital Market 
            Authority (CMA). Of this, direct premium of general insurance sector 
            soared by 15.6 per cent to RO100.75 million, while life insurance 
            sector showed a moderate 5.3 per cent growth at RO16.52 million in 
            2005 over the previous year.<br>
            <br>
            Likewise, the aggregate assets of insurance companies soared by 19 
            per cent to RO208.41 million by end-2005 over the previous year. Of 
            this, the assets of non-life segment moved up by 23.2 per cent to 
            RO157.99 million, while assets of life segment rose by 6.7 per cent 
            to RO50.42 million.<br>
            <br>
            The insurance penetration in Oman is relatively low at less than two 
            per cent, which is lower than many developing countries. Yet another 
            factor that could lead to a better demand for insurance product is 
            the fast development of gas-based industries, especially at the 
            emerging Sohar Industrial hub. It is estimated that capital 
            investment worth more than US$12 billion is being pumped in Sohar 
            alone for building various gas-based industries. But most of the 
            benefits from these projects accrue to national companies as 
            partially state-owned companies prefer national companies for their 
            insurance needs. <br>
            <br>
            As far as the demographic feature of Oman is concerned, around 55 
            per cent of the Sultanate’s population is under 20 years of age and 
            nearly a third of them are students. This is another positive factor 
            that could help the growth of life insurance segment in the coming 
            years.</font></p>
