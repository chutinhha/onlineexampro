            <p align="justify" style="margin-left: 10px; margin-right: 10px">
            <font face="Verdana" size="1">Oman’s insurance sector is going through a transitional phase with 
            more players entering the market, even as national firms are 
            strengthening their capital base to compete in a liberalized 
            environment. After Oman’s entry in the World Trade Organisation (WTO), 
            several regional players consider the country as a potential market.<br>
            <br>
            Few multinationals that had been operating through agency agreements 
            have strengthened their position by forming joint ventures with 
            local partners, while others are set to float new insurance firms to 
            grab a slice of the RO117 million premium income. <br>
            <br>
            <b>New Entrant</b><br>
            The latest company to enter the fray is Al Madina Gulf Insurance 
            Company, which is promoted by four <br>
            <br>
            pan-GCC investors in association with local partners. With a capital 
            base of RO10 million, the joint venture firm is promoted by 
            Bahrain-based Solidarity, Qatar–based Doha Bank and Qatar Islamic 
            Insurance and Oman’s MB Petroleum, Al Madina Financial and 
            Investment, Diwan of Royal Court, Transgulf Co and Ministry of 
            Defence Pension Fund. <br>
            <br>
            The three foreign promoters have a combined stake of 25 per cent in 
            the joint venture. To start with, the newly floated firm will offer 
            general insurance products. But it has a CMA licence for both life 
            and non-life businesses.<br>
            <br>
            It was only last year that Al Anwar Holding formed a joint venture 
            general insurance firm with a capital base of RO5 million. The new 
            firm — Falcon Insurance — was established by taking over the Muscat 
            branch operation of a Lebanese firm — Al Ittihad Al Watani. The move 
            is viewed as an attempt by Al Anwar (which has 30 per cent or RO1.5 
            million equity stake in Falcon Insurance) to strengthen its position 
            in insurance field, after the collapse of its associate — ONIC 
            Insurance — few years ago.<br>
            <br>
            After the collapse of ONIC Insurance, few regional and multinational 
            players strengthened their position in the Sultanate. For instance, 
            Royal &amp; SunAlliance formed a joint venture with two Omani partners — 
            W J Towell &amp; Co and Oman Holding International (OHI) — to strengthen 
            its position in the local market. The company was operating in the 
            country for three decades through an agency agreement with W J 
            Towell. Oman Qatar Insurance Company is another major player that 
            entered the Sultanate to get a slice of the market.</font></p>
