        BMW – just a click away<br>
        <img border="0" src="pic33.jpg" align="right" hspace="5" vspace="2" width="150" height="101"></b>There’s good news for all BMW lovers. BMW Group Middle East has 
        launched its innovative website, BMW-ME.COM, an innovative in-depth 
        portal giving customers an interactive and enhanced BMW experience. <br>
        <br>
        BMW-ME.COM provides extensive information and full specifications of all 
        BMW vehicles, accessories and pre-owned vehicles. In addition, customers 
        can access the site to download the BMW owner service and warranty 
        manual, catch up on the latest BMW innovations and source details on the 
        BMW Group Middle East’s importer network across the region. <br>
        <br>
        The website also features interesting sections right from the Innovative 
        Lounge — where all questions on Active Steering and xDrive are answered 
        and remotely experienced to the Technology Guide, which provides concise 
        and in-depth explanations on all the latest innovations and safety 
        systems available in BMW cars. BMW Group Middle East has more reasons to 
        celebrate as 2006 seems to be another record sales year with BMW Q3 
        deliveries to customers in the region up by 12 per cent year on year. 
        Inspite of major political and social problems in the region, retail 
        sales of BMW Group Middle East vehicles were 3606 units in the period 
        July to September 2006, with the new BMW 7 Series, BMW 5 Series, BMW X5 
        and BMW 3 Series driving growth.<br>
        <br>
        Oman is the fastest growing market in the Gulf Cooperation Council 
        region with sales accelerating ahead by 30 per cent, compared to the 
        same period in 2005, followed by Qatar, which is 17 per cent up. Dubai 
        also saw a rise in sales in the third quarter of this year. And adding 
        to this excitement is the launch in the fourth quarter of two new models 
        — the BMW M6 Convertible and the BMW X3 LCI, followed by the phenomenal 
        launch of the new BMW X5 and the new MINI in 2007. <br>
