        <b>ACCENT IN A NEW DESIGN</b><br>
        A redesigned Accent awaits all Hyundai enthusiasts. The auto company has 
        totally revamped the Accent, with new styling, new interior, more 
        powerful engine, sophisticated suspension, and more standard safety 
        equipment.<br>
        <br>
        The Accent’s 1.6-liter 4-cylinder DOHC engine, with continuously 
        variable valve timing, now produces 112 bhp and while it idles quietly 
        and cruises in relative silence, under hard acceleration it definitely 
        makes itself heard.<br>
        <br>
        The 2007 Accent offers more interior roominess and remarkably large 
        trunk space thanks to a taller stance. It has a handsome, rounded 
        profile featuring a roofline that sweeps from hood to trunk in one long 
        curve. Accent comes with a 4-speed automatic, which is more than ample 
        for most folk. It has better fuel mileage on the highway.<br>
