        <b>Proceed with Kia</b><br>
        <img border="0" src="pic26.jpg" align="left" hspace="5" vspace="2" width="125" height="84">Kia Motors’ latest concept car pro_cee’d debuted at the Paris Motor Show 
        later this month. The strikingly styled showcar is based on the 
        forthcoming three-door ‘sporty hatch’ version of the all-new Kia cee’d 
        C-segment model. This five-door family hatchback will be manufactured at 
        Kia’s first-ever European assembly plant in Slovakia from November 2006.
        <br>
        <br>
        Following on from the original cee’d concept car exhibited at the Geneva 
        Show earlier this year, Kia’s brief to the designers for pro_cee’d was 
        to create a ‘teaser’ for the all-new Kia cee’d ‘sporty hatch’ which will 
        become the third model in the cee’d line-up (after the five-door and 
        wagon) from December 2007. The pro_cee’d looks similar to Kia’s new 
        cee’d ‘sporty hatch’ model with its dynamic and masculine nature. The 
        wheels are a two-part design, featuring five double-spokes arranged with 
        a recessed black power-coated centre and brightly polished convex 
        perimeter spokes. Key attributes, which the pro_cee’d and ‘sporty hatch’ 
        models will have in common, include the lowered roof line (down by 25 mm 
        compared to the five-door cee’d) and shallower side windows ringed in 
        chrome trim that creates a robustly sporty window graphic.<br>
