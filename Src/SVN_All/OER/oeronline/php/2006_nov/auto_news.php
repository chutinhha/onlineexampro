        <b>A SAFE DRIVE FROM SUBARU </b><br>
        The All-Wheel Drive Subaru Forester has ranked highest in Used Car 
        Safety Ratings (UCSR) in a report that was made public recently. For the 
        first time in the study’s 15-year history, a ‘standout’ car was 
        identified, with Subaru’s Forester rating as top performer. <br>
        <br>
        The UCSR is the world’s largest study of vehicle crashes and is 
        conducted by the Monash University Accident Research Centre and 
        supported by the RACV, the Transport Accident Commission (TAC), and 
        VicRoads, as well as other state and federal road authorities and 
        motoring clubs from Australia and New Zealand. <br>
        <br>
        Subaru Liberty and Outback were also named as vehicles offering well 
        above average protection to their drivers in a crash. Like Forester, 
        current generation Liberty and Outback also have five-star ANCAP 
        crashworthiness ratings for occupant safety. <br>
