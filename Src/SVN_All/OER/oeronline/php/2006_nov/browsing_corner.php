                <p align="justify" style="margin: 5px">
                <font face="Verdana" size="1">“This is the most important issue in marketing today. The path 
                you choose has enormous implications for the success of your 
                brand. <br>
                <br>
                Creating a new category and then making sure that your brand 
                name stands for that new category in the mind is not easy. 
                Especially today when the emphasis in product design and 
                development is not on creating new categories, but on combining 
                existing categories. <br>
                <br>
                If categories are converging, the existing brand names (and the 
                companies that own these brand names) are going to become more 
                powerful. If the handheld computer (or PDA) converges with the 
                cellphone (as many pundits have predicted), then the resulting 
                PDA/ cellphone market would likely be dominated either by Palm, 
                the dominant PDA brand or by Nokia, the dominant cellphone 
                brand.<br>
                <br>
                If categories are diverging then the opportunity arises for new 
                brands. You know where our beliefs lie. In spite of the 
                overwhelming media hype to the contrary, we strongly believe 
                that convergence will never happen. Technologies don’t converge. 
                They diverge. And thankfully they do, otherwise it would be 
                almost impossible to create new brands. <br>
                <br>
                In the best of circumstances, branding is not an easy task. 
                Especially today when the convergence philosophy has made such 
                enormous inroads in the corporate psyche.” </font></td>
