            </font></b><font face="Verdana"><b><font style="font-size: 14pt">The 
            Origin of Brands</font></b></font><font face="Verdana" size="1"><br>
            <b>Brands live, evolve and die just like humans, argue authors Al 
            and Laura Ries</b><br>
            &nbsp;</font></p>
            <table border="0" cellpadding="0" cellspacing="0" style="border-collapse: collapse" bordercolor="#111111" width="100%" id="AutoNumber1">
              <tr>
                <td width="65%">
                <p align="justify"><font face="Verdana" size="1">Marketing experts Al and Laura Ries have dedicated their book to 
            ‘Divergence, the least understood and most powerful force in the 
            universe’. And in doing so, they have applied Charles Darwin’s 
            famous Origin of Species theory to understand how product evolution 
            creates endless possibilities for new brands. “Comparing branding 
            with biology might seem far fetched, but we could think of no other 
            analogy that so clearly and simply explains the branding process,” 
            say the father and daughter duo. <br>
            <br>
            Going through the book you will be left in no doubt that just as 
            species evolve, diverge and sometimes altogether disappear, so do 
            brands! Darwin has convinced us all how mankind has evolved over the 
            years. Twigs flourish when a tree is a mere bush and grow into many 
            branches; later some branches decay and fall off while others 
            continue to live in modified conditions. Ditto for brands, say the 
            authors. They start with the story of the typewriter, which diverged 
            to form multiple branches — manual typewriter, portable typewriter 
            and electric typewriter. Overshadowed by the personal computer, the 
            typewriter branch has now decayed and is about to drop off.</font></td>
                <td width="35%">
                <p align="center"><font face="Verdana" size="1">
            <img border="0" src="pic10.jpg" hspace="5" width="150" height="214"><br>
                <b><i>THE ORIGIN OF BRANDS<br>
                Authors: Al and Laura Ries<br>
                Published by: HarperCollins<br>
                Pages: 295<br>
                Price: RO6.300</i></b></font></td>
              </tr>
            </table>
            <p align="justify" style="margin-left: 5px; margin-right: 10px; margin-top:5; margin-bottom:5"><font face="Verdana" size="1"><br>
            The computer, in turn, along with television, radio and telephone, 
            has gone through these stages of evolution and divergence. Just as 
            one does not see two branches converge to form a single branch, it 
            is highly unlikely these products will ever converge successfully. 
            How many people do you know who own a fax-cum-phone? Very few! The 
            output is just not the same. How can then one predict which brand 
            will survive, or die, or diverge to form a new category altogether? 
            Can market research predict the future? The authors are certain it 
            cannot. Indeed, it is very difficult to do so. Market research is 
            only useful to explore the past and even test marketing has more 
            negatives than positives, they warn. <br>
            <br>
            The best way to create a powerful new brand is to look for ways in 
            which your product or service can diverge from an existing category 
            and become number one; to divide a market and then conquer it; and 
            finally, to overcome the notion of serving a market to create a new 
            market instead is what is important. They give examples of how the 
            biggest brand ever, Coca-Cola, did not serve an existing market but 
            rather created a new one called cola. And how Microsoft didn’t get 
            mighty by building a better personal computer operating system. 
            Macintosh was far superior, but Microsoft won by being the first.<br>
            <br>
            Brand building isn’t easy, however simple it may sound when you read 
            this humorously written book. When to prune, when to push forward, 
            the importance of a launch and PR exercise to precede the launch and 
            the naming of the brand are important aspects to be diligently 
            worked out. <br>
            <br>
            From a well-illustrated picture of why bad ideas never die, to 
            excellent examples of countless brands, the authors take us on a 
            journey that illustrates the importance of being the first in the 
            mind. Remember Hertz ‘rent-a-car’, Harvard University, Mikimito 
            pearls and Hewlett Packard? All no longer first, but have the 
            fortune of being the first — because when we think about renting a 
            car we think of Hertz, or while buying pearls it’s Mikimoto that 
            comes to the mind.<br>
            <br>
            The authors caution that ‘fitness’ always takes precedence over ‘firstness’. 
            Strategically, placing a second brand exactly opposite the leader is 
            a wise observation, as nothing can grow even under the shadow of the 
            first. Apeing the leader can bring disastrous results, they point 
            out. Burger King copies everything that McDonald implements — only 
            they can’t copy the revenue! Establishing the enemy keeps the brand 
            on its toes. <br>
            <br>
            After the brand is in the market, it is also essential to know when 
            to exit — just as one would prune a tree to keep it in shape and 
            healthy, and discard the rotten parts! Is a brand’s life more 
            important vis-à-vis the company’s? Is it wiser to create a new brand 
            or a new category? Is it profitable and/or wise to go for line 
            extensions? Is it better to be a generalist or specialise in a 
            particular field? What kind of a name generates revenue? Should it 
            be long or short? While packaging the product, should it be 
            different or same as the leading brands?<br>
            <br>
            From the Japanese model to the American, from doctors and lawyers to 
            marketing specialists, the authors have dug up several case 
            histories to make it a worthwhile book and yet kept it simple for 
            non-marketing people. So read and enjoy what’s on offer. </font></p>
            <p align="right" style="margin-left: 5px; margin-right: 10px; margin-top:5; margin-bottom:5"><font face="Verdana" size="1">
            <i>— Uma Devi Jadhav</i><br>
            &nbsp;</font></p>
