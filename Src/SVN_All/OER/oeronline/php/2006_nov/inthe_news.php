<p align="justify" dir="ltr" style="margin-left: 5; margin-right: 5"><b><br>
<font face="Verdana" size="4">A new chapter</font></b><font face="Verdana" size="1"><b><br>
<br>
US President George W Bush on September 26 signed the historic US-Oman FTA 
legislation into law, paving way for a new chapter in trade ties with the 
Sultanate<br>
</b><br>
Oman and the United States moved into a new phase of bilateral trade ties when 
the US President George W Bush on September 26 signed legislation containing the 
historic Oman-US Free Trade Agreement into law during a ceremony at the White 
House in Washington. President Bush lauded the Free Trade Agreement saying: 
“This Agreement will level the playing field for US workers and businesses, 
provide additional market access for US goods, help Oman develop long-term 
opportunities for its people, and advance our shared goal of building a Middle 
East Free Trade Area.”<br>
<br>
Earlier, the US Senate gave final congressional approval on September 19 to the 
FTA with Oman, sending the bill to President George W Bush to sign into law. 
Senate Majority Leader Bill Frist said the agreement would help the US “advance 
our goal of a freer and more open Middle East”. He praised Oman as a long-time 
friend of the US and a committed ally “in the global war on terror”.<br>
<br>
The Senate voted 63-31 to approve the trade deal, which will immediately 
eliminate duties on consumer and industrial goods traded between the United 
States and Oman on the first day the pact takes effect.<br>
<br>
The agreement will break new ground for Oman-US bilateral trade relations, 
expanding export opportunities for both the US and Omani firms. The agreement 
will also provide new opportunities for services, provide a secure and 
predictable legal framework for investment, provide for effective enforcement of 
labour and environmental laws, and protect intellectual property. This free 
trade agreement will enter into force when all of the applicable legal 
requirements and procedures have been completed.<br>
<br>
The deal also opens the Omani market to more US business in service sectors like 
banking and <br>
telecommunications. Two-way agricultural trade will be nearly duty free from the 
first day and completely duty free within 10 years.<br>
<br>
This is the fifth US free trade agreement in the Middle East and the fourth with 
an Arab country following recent pacts with Jordan, Morocco and Bahrain. <br>
<br>
Free trade talks with the UAE stalled earlier this year when US lawmakers forced 
Dubai Ports World to abandon plans to purchase control of certain US port 
operations.<br>
<br>
The US House of Representatives had narrowly approved the Oman agreement in July 
by a 221-205 vote.</font></p>
