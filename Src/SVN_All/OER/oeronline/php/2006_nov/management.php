<p align="justify" dir="ltr" style="margin-left: 5; margin-right: 5"><b>
<br>
</b><font face="Verdana"><b><font style="font-size: 14pt">Anticipating the Next 
Leadership Wave</font></b></font><font face="Verdana" size="1"><br>
<br>
<img border="0" src="pic45.jpg" align="right" hspace="5" width="100" height="147">We may be in a quite different world two years from now. Global politics and 
economics are on a knife edge. When things shift, supposedly new management 
ideas will quickly follow. The pundits will exhort us to adopt new approaches to 
deal with the rapidly changing scene. <br>
<br>
But leaders who know what they are doing will not jump on the bandwagon. They 
will determine what their business needs to deal with developments in their 
particular environment. After all, what is important in one area of the world 
may not be important in another.<br>
<br>
The real challenge is not to get sucked into the popular fad of the moment, but 
to craft the kind of leadership practice that is appropriate for your business 
context and your preferred management style.<br>
<br>
This is not rocket science, but a matter of applying six simple guidelines:<br>
<b>Know your political and economic context.</b> Be fully aware of how political 
and economic developments are affecting people in different parts of the world. 
Some of us don’t spend enough time getting into the skins of other people. We 
allow a subconscious sense of cultural superiority to blind us to the changes in 
different markets and how they affect customer and employee behaviour — and, 
hence, the appropriate management practice.<br>
<br>
<b>Be aware of the evolving economic cycle.</b> Look for accelerated changes in 
the economic cycle. In periods of a rising sociopolitical uncertainty, a simple 
extrapolation of past trends into the future is increasingly questionable. And 
the effects of sharp shifts will vary. In some industries, there may be new 
opportunities to innovate and penetrate new markets while competitors are frozen 
in their tracks. For others, process efficiency and cost-cutting may again be 
essential.<br>
<br>
<b>Identify the unique features on the supply side of your business.</b> What 
are the critical factors that shape the industry’s dynamics? Capital intensity? 
Employee productivity? Is the industry knowledge-driven? How will the critical 
drivers be affected by political and economic developments?<br>
<br>
<b>Anticipate the impact of new technology.</b> Despite political and economic 
pressures, new technology usually enlarges the opportunities for growth. And 
when it enhances communication and information, it supports global growth. The 
first-mover opportunities associated with fundamental innovation often trump the 
negative trends.<br>
<br>
<b>What do the forces of change add up to?</b> How strong are these forces? Do 
you have to shift to a new strategy or reinvent the business model? Are your 
people up to speed? How ready is the organisation? Your leadership practice has 
to exploit and fit in with the demands of the required change management, no 
matter what the management ideas of the moment.<br>
<br>
<b>Know who you are, your personality and preferred style.</b> We all have to 
play to our strengths — whether we are a company, a team or an individual. Being 
true to oneself as a leader works best. This also means realising when your 
preferred style is wrong for the context. If the situation demands a big 
stretch, you may be better off bringing along someone on to your team with a 
more appropriate style to complement yours.<br>
<br>
Whatever the new world we may be living in, it will provide opportunities for 
leaders who can sense what’s needed and how they can stretch to adapt. Can you 
anticipate the next type of leadership practice that will matter for you? Will 
you have the courage to stand out from the pack to build on your business and 
personal strengths?<br>
<br>
