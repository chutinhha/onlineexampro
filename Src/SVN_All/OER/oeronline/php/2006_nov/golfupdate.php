<font face="Verdana" style="font-size: 14pt">
Ladies Masters <br>
</font><font style="font-size: 14pt" face="Verdana">Take Centrestage at Dubai</font><font face="Verdana" size="1"><br>
More than 120 ladies golfers slugged out at Dubai Ladies Masters to mark the 
debut of LPGA tour in the Middle East</font><font face="Verdana" style="font-size: 14pt"><br>
<br>
</font>
</b><font face="Verdana" size="1">
<img border="0" src="pic37.jpg" align="right" width="200" height="133">The Dubai Ladies Masters held at the Emirates 
Golf Club’s Majlis course, on October 26-29, 2006 ended on the expected lines. 
Annika Sorenstam won by six strokes after shooting a 3-under 69 on the final 
day. The top-ranked Swede, who led for the entire tournament, had four birdies 
in the final round to finish at 18-under 270 total. Helen Alfredsson (67) was 
second at 276, followed by Karrie Webb (70) at 278.<br>
<br>
“I was quite comfortable out there,” Sorenstam said. “With a seven-shot lead, I 
knew all I had to 
<img border="0" src="pic38.jpg" align="left" hspace="5" vspace="2" width="200" height="152">do was not post a big number on any hole. But I was solid and 
hit a lot of greens, which made it easier.” Alfredsson, the captain of the 2007 
European Solheim Cup team, birdied the first two holes but and then dropped 
shots on the next two. She later added five birdies to overtake Webb.<br>
<br>
Amy Yang (69) finished with back-to-back birdies to tie for fourth place with 
Veronica Zorzi (70) at 280. Laura Davies won the Ladies European Tour Order of 
Merit and Nikki Garrett of Australia won the Rookie of the Year award. <br>
<br>
The LPGA Tour was held in the Middle East for the first time, and it also marked 
the tour’s season-ending event. The event offered prize money worth US$629,000 
making it one of the top five richest events on the Ladies European Tour. 
Promoted and organised
<img border="0" src="pic39.jpg" align="right" hspace="5" vspace="2" width="200" height="165">by ‘Golf in Dubai’, the tournament featured an elite 
group of 120 golfers.<br>
<br>
The official airline Emirates organised an exclusive Pro-Am tournament on 
October 25. Ghaith Al Ghaith, Emirates’ executive vice-president commercial 
operations worldwide said: “We are proud to be able to support the first Dubai 
Ladies Masters, and help reinforce Dubai’s position internationally as one of 
the most sought-after destinations by the world’s leading golfers. As a 
prominent supporter of top golf events around the world, including the Dubai 
Desert Classic, our involvement with this event closely fits our strategy of 
expanding our golf sponsorship portfolio.”</font></p>
