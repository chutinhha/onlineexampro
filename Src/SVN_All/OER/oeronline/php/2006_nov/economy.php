<p align="justify" dir="ltr" style="margin-right: 10px"><font face="Verdana" size="1">
<br>
<br>
</font><b><font style="font-size: 16pt" face="Verdana">Diversification Anchored 
in Export</font></b><font face="Verdana" size="1"><br>
<b>Thanks to its export-oriented industrial strategy, the Sultanate is on the 
right track to achieve its goals of economic diversification</b><br>
<br>
<img border="0" src="pic62.jpg" align="right" hspace="5" width="120" height="158">Oman is developing its local manufacturing and industry capacity to improve its 
export potential. The government’s efforts are in line with the objective of 
economic diversification, specifically a move away from a reliance on oil, which 
is anchored in export oriented industries. <br>
<br>
In a significant step to facilitate exports, the Oman International Container 
Terminal (OICT) at the Port of Sohar opened for business on October 16 this 
year. The new terminal will cater to the large industrial projects in the Sohar 
Industrial Port as well as the Sohar Industrial Estate, while allowing other 
industries along the northeast coast to use the facilities for export. <br>
<br>
In 2005, total exports were valued at RO7,187 billion. The value of exports have 
increased considerably this year with the latest figures, from the Ministry of 
National Economy, showing a 29.6 per cent increase in Q1 2006 on Q1 2005 — 
mostly due to the surge in oil prices.<br>
<br>
<img border="0" src="pic63.jpg" align="right" hspace="5" vspace="2" width="100" height="47">While the headline increase in non-oil exports registered an impressive 32.1 per 
cent in 2005, the base figure of RO420.3 million in 2004 is so low that it takes 
some of the gloss off the achievement. <br>
<br>
Historically, the most significant export markets for the Sultanate were other 
GCC countries. Some observers expect this to diversify in the near future. <br>
<br>
For example, the Oman India Fertilizer Company (OMIFCO) exports to India made 
that country the second largest market for the Sultanate in the first four 
months of 2006 on a year on year basis, replacing Saudi Arabia, while the UAE 
maintains the top spot. <br>
<br>
The RO1 billion project, a joint venture of Oman Oil Company (with 50 per cent) 
and two Indian cooperative fertilizer companies, was inaugurated on January 28, 
2006. Production is exclusively destined for export to India under a fixed 
agreement for 10 years. <br>
<br>
Exports to India in the first four months of 2006 amounted to RO36.5 million, an 
1116.7 per cent increase from the RO3 million recorded for the same period in 
2005, a direct reflection of the fertilizer project.<br>
<br>
A series of mega industrial projects will be coming online in Sohar in the 
coming years, which will further alter Oman’s export orientation. The first were 
launched this autumn with the refinery and the polypropylene plant, which are 
beginning commercial production. This will be followed by projects in aluminium 
and polyethylene, among others, which will also be intended for export.<br>
<br>
These heavy industry projects will also cater to downstream industries in the 
vicinity. Proposals for a ‘plastic park’ have been put forward to create value 
added products from the raw materials of the heavy industry. These will be open 
in the special economic zone within the port complex and the Sohar Industrial 
Estate. There will be a lot of opportunity for SME development as downstream 
activity from heavy industry, HE Maqbool Ali Sultan, the Minister of commerce 
and Industry, told OBG. <br>
<br>
The Sohar Aluminium Company, once completed, would be able to produce 350,000 
tpa of liquid metal, which will feed into local industry. The company has high 
expectations for demand of its product and has set an output target of 60 per 
cent. <br>
<br>
These SMEs manufacturers will also need to target the export market. This will 
benefit the sultanate in two ways, adding further value to the products will 
retain more revenue and increase potential for employment opportunities.<br>
<br>
In Saudi Arabia, where a plastic industry (producing both polypropylene and 
polyethylene) is well established, it has been estimated that up to 700 
downstream companies have been formed, creating more than 70,000 jobs. <br>
<br>
The economics of setting up heavy industry dictate a size and rate of output 
that could never be satisfied domestically in a country of only 2.5 million 
people. Those same economics dictate that exploring external markets has an even 
greater significance than the actual setting up of non-oil industries. <br>
<br>
The Vision 2020 projected a reduced contribution of oil revenue as a percentage 
of GDP, from 33.5 per cent in 1995 to 9 per cent in 2020. Given the record high 
oil prices however, oil profits constituted over 40 per cent of the GDP in 2005 
and it is unlikely that the Vision 2020 will be met in terms of percentages of 
oil contribution in GDP if oil prices stay at their present levels. <br>
<br>
However, as the oil price rises and increases in efficiency improve 
recoverability rates for oil, the targeted reduction in the ratio of oil revenue 
to GDP becomes less significant. Economic diversification does not merely entail 
a proportional reduction in oil revenue contributions to GDP, but also relies on 
the growth in absolute terms of the non-oil sector. Thanks to its export 
oriented industrial strategy, the Sultanate seems on the right track to 
achieving these goals<br>
