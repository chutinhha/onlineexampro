<p align="justify" dir="ltr" style="margin-left: 10px; margin-right: 10px"><font face="Verdana" size="1">
<br>
</font><font face="Verdana"><b><font style="font-size: 14pt">Dial L for Luxury</font></b></font><font face="Verdana" size="1"><br>
<b>With the IS 300, Lexus enters a new segment, providing comfortable and 
luxurious ride with a sporty edge. Anne Kurian test-drives the latest speedster 
in town</b></font></p>

<p align="center" dir="ltr" style="margin-left: 10px; margin-right: 10px">
<img border="0" src="pic35.jpg" width="300" height="185"></p>

<p align="justify" dir="ltr" style="margin-left: 10px; margin-right: 10px"><font face="Verdana" size="1">Lexus has always been associated with luxury, sophistication and comfort. Sporty 
driving is not something you would want to try out with your gleaming Lexus 
sedans. Not that the vehicles aren’t equipped to handle a bit of rough ride. But 
you would rather picture it as traveling in a Boeing 767. <br>
<br>
The brand new Lexus IS 300 is a class apart, though. Coupled with the 
pleasurable driving experience of a Lexus is a new sporty touch in the Lexus IS 
300. Add the precision engineering of Lexus and optimum luxury to that and you 
have a car that definitely is a chart topper.<br>
<br>
Leaving aside all the finer aspects of the IS 300, there’s something else that 
makes this new addition to the Lexus family a car to watch out for. The IS 300 
is Lexus’ answer to its competitors and brand loyalists in the small car luxury 
segment. The Lexus never had a 4-door sporty sedan till 2000, a choice that 
luxury brands had. But not to be left behind, Lexus brought forth the IS to 
challenge sedans of the same category. The IS has been around in the US and 
European markets since 2000 but it made its Middle East debut just a few days 
before this Ramadan. So has the wait been worth it? After a good six hours with 
the IS 300, we can say this is one vehicle that’ll capture the Middle Eastern 
driver’s fancy. If it hasn’t already!<br>
<br>
<b>The Outside-Inside Story</b><br>
The IS 300 sports an unmistakable racy look, which can be attributed to its 
chiseled nose and overall form. The first glance gives off the impression that 
the IS just can’t wait to get started. And once you step inside the sedan, you 
end up feeling the same. <br>
<br>
What also add to its sporty look are the short front and rear overhangs. The IS 
300, as with other Lexus models, is designed under the L-Finesse philosophy. 
This philosophy is about creating something that’s unique and creates a deeper 
relationship with the driver. The designers of Lexus intend to not just create a 
visually arresting vehicle but also an automobile that balances out luxury, 
styling and driving experience well, each complementing the other.<br>
<br>
The IS 300 is also a smaller car compared to the others in the family but that’s 
explicable if one takes into consideration the fact that it is styled along the 
lines of a sports car. Accentuating the unique look of the IS 300 are the 
wraparound trapezoidal headlamps and the equally aesthetic rear lamps.<br>
<br>
The world inside the IS 300 is again all about luxury and comfort. Leaving aside 
the keyless entry factor, right from the plush leather trimmings and silver 
accents to the aesthetically placed control knobs on the dashboard to the 
ventilated front seats, the IS 300 impresses you totally. The instrument panel 
cluster features two round analog meters inset with fuel and temperature gauges. 
Quite a cool design, we must say. After all, the IS 300 is designed to cater to 
those who like to live it up in life!<br>
<br>
Another interesting aspect of the IS 300 is the quiet interiors, even when 
cruising at considerable speed. The creators at Lexus have said that to achieve 
this effect, every facet of the body, including the shape of the door mirrors, 
were taken into consideration. In fact, the sunvisors have a noise-absorbing 
structure to reduce the acoustics from inside and outside. <br>
<br>
Even the windshield glass has been incorporated with a high noise-damping type 
inner film. All these aspects also bring into focus the excellent Mark Levinson 
surround sound system that elevates the musical experience inside the IS 300. 
With the least possible exterior noise inside the car and 14 speakers to boot, 
you can be sure to take the longer route back home drive, especially if you are 
a music lover (like us).<br>
<br>
<b>On The Tarmac </b><br>
Revving up the engine and trying to rough it out in a Lexus is something not 
many of the brand’s aficionados would really try. For the mere fact that Lexus 
is more about class steeped in comfort. The IS 300 rewrites this rule by 
entering the sports sedan category and is set to give its brand opponents a run 
for their money.<br>
<br>
While driving the IS 300, the first impressive feature that caught our attention 
was its agility. The IS 300 moves with complete ease and is engineered to take 
on minor (and slightly bigger) bumps with élan. The driving experience only got 
better as we put the IS 300 to test on the automobile-cramped roads of Qurum-Al 
Khuwair and then on one of the favourite test drive stretches of many car 
companies — the road to Qantab. Climbing the steep path to Qantab was of no 
trouble for the IS 300, that too with commendable dexterity.<br>
<br>
The road handling of the IS 300 was impressive too, which we tested out at a few 
sharp curves and also the roundabouts. This is where we actually felt the 
stability provided by the front double wishbone suspension and rear multi-link 
suspension.<br>
<br>
<img border="0" src="pic34.jpg" align="right" hspace="5" vspace="2" width="200" height="136">Another one of the IS 300’s irresistible feature is the 6-speed multi-mode 
automatic transmission, with paddle shift switches on the back of the steering 
wheel. The paddle switches actually give you the feeling of being in the seat of 
a FI racing car. You have the option of driving in manual mode while at the same 
time shifting gears without taking your hands off the steering wheel. We tested 
out the Sport mode too and were equally impressed with the acceleration and racy 
feel.<br>
<br>
<b>Safe And Sound</b><br>
Lexus gives safety optimum a priority and leaves nothing to be desired. The 
safety aspects of the IS 300 have been developed under the company’s GOA (Global 
Outstanding Assessment) philosophy, which, they say, exceed the safety standards 
of most countries. As per Lexus, the IS 300 has been incorporated with the 
highest level of safety body in its category. The sedan also comes with the 
other safety features like traction control system, vehicle stability control, 
front seats side and knee airbags, and adaptive front-lighting system (where the 
lights tilt as per the car’s turning radius), to name a few.<br>
<br>
On a final note, the Lexus IS 300 has an impeccable driving quality complemented 
by important features like comfort, luxury, sporty feel and top-of-end safety 
features. This speedster is going to get the adrenaline pumping for many car 
enthusiasts out there!</font></p>
