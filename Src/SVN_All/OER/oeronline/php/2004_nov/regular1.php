Operational Risk<br>
</font></font><font face="Verdana" size="1">
<br>
<b>Ali Hassan Moosa explains why banks and other financial intermediaries need 
to manage operational risk as means to add value to the business</b></font></p>

<p align="justify"><font face="Verdana" size="1">
<img border="0" src="im80.jpg" align="left" hspace="5" width="125" height="147">Operational 
risk poses one of the greatest challenges that financial intermediaries face 
today. Until recently, operational risk was widely misunderstood, arguably 
because it was loosely defined. Furthermore, and for a verity of reasons, boards 
and senior management previously neglected a large number of operational risk 
elements. <br>
<br>
However, things are changing, and there have been encouraging signs that signify 
operational risk to be as significant as other risks that were earlier perceived 
as more important to the organisation such as credit risk and financial risks, 
mainly liquidity and interest rate risks. <br>
<br>
The Basel Committee, an international committee that had played a leading role 
in standardising bank regulations across the globe, has defined operational risk 
as: ‘‘The risk of direct or indirect loss resulting from inadequate or failed 
internal processes, people and systems or from external events.’’<br>
<br>
The Basel Committee first showed interest in operational risk back in 1998. 
Moreover, in February of 2003, a key document was published by the committee 
titled “Sound Practices for the Management and Supervision of Operational Risk”.<br>
<br>
Locally, the apex bank, the Central Bank of Oman (CBO), issued comprehensive 
risk management guidelines in May 2003 to all licensed banks operating in the 
Sultanate. These guidelines included an operational risk management framework.<br>
<br>
<b>The framework</b><br>
Within the complex environments of financial institutions, there could be 
numerous examples of operational risks that may include erroneous transaction 
processing, malfunction of IT and telecommunication systems, fraud 
(employee/customer/third-party), theft, unauthorised activities (lending, 
trading, etc.), loss of assets, loss of data (physical/logical), workplace 
safety, fire, natural disasters, and acts of terrorism.<br>
<br>
Operational loss could lead to reputation loss, leading to dire consequences of 
market share loss and a drop of the share price, and eventually resulting in an 
irreversible damage to shareholder value. <br>
<br>
As far as corporate governance is concerned, board and senior management 
oversight of the risk management framework is of paramount importance. The risk 
framework itself should be an autonomous function through a separate specialised 
department or a committee. Furthermore, internal and external audits need to 
carry out independent testing and verification of all processes. As far as 
operational risk is concerned, exposures, losses, and risk indicators should 
regularly be reported to the board.<br>
<br>
<b>The approach</b><br>
Practically, the optimal approach, in my view, is to manage operational risks 
within the departments in which they arise. For instance, treasury back-office 
function is best suited to manage settlement risk; IT professionals are best 
suited to address systems related risks. <br>
<br>
However, overall planning, coordination, and monitoring should be centralised at 
a corporate level and through a well-designated risk management function that 
integrates operational risks with other risks such as credit, liquidity, 
interest rate, and market. <br>
<br>
For the first time, the new capital accord under Basel II puts more weight on 
operational risk than previously acknowledged. Credit, market, and operational 
risks are the basis on which the first pillar of the accord is constituted – the 
minimum capital requirements. The other two pillars of the accord are the 
supervisory review process and market discipline.<br>
<br>
The new accord proposes the usage of Advanced Measurement Approaches (AMA) for 
calculating operational risk charge to the capital. It is widely believed that 
institutions using the AMA will have considerable flexibility in setting up 
measurement systems that are more appropriate to the nature of the business and 
size of the organisation.<br>
<br>
Modern methods of risk-adjusted pricing extend the pricing model beyond credit 
risk. In more developed economies, some financial intermediaries with high 
volume of transactions incorporate a premium for operational risk in their 
product pricing. <br>
<br>
<b>The challenges</b><br>
Measuring operational risk is a relatively new concept in this part of the 
world. The concern is that local and regional banks may perhaps shy away from 
innovation and/or increase pricing in a manner that may not be well perceived by 
the market. <br>
<br>
Risk assessment is a technique used in order to identify risks, their potential 
event probability and impact on the organisation. The method provides a useful 
tool to identify vulnerabilities/exposures, assess the risks to the business, 
and identify means so as to avoid or reduce such risks.<br>
<br>
There has been a considerable debate among risk managers on whether to assess 
operational risks with qualitative or quantitative methods. The conclusion 
reached, it seems, is that certain event possibilities are best measured through 
quantitative techniques. Application examples include statistically modelling 
frequencies of errors happening in customer accounting at the branch level. <br>
<br>
On the other hand, other possibilities that are non-uniform in nature, such as 
acts of terrorism and natural disasters that are uncommon to a geographical 
region cannot be modelled accurately, and thus, qualitative techniques may best 
suit the assessment of such events.<br>
<br>
Risk assessment is executed by performing a structured analysis of the 
organisation through ‘what if’ scenarios. The resulting risk synopsis can then 
be plotted against probability and impact axis to help prioritise 
risk-mitigating actions. It is obvious from the risk assessment chart that 
events plotted in the ‘High” likelihood and impact zones, if neglected, could 
have catastrophic consequences.<br>
<br>
Insurance plays a vital role in risk mitigation. It is a cost-effective 
financing method for mitigating operational risk, lowering the cost of capital 
and reducing volatility. By insuring elements of operational risk, a bank can 
free up capital that could have been otherwise blocked to cover operational 
losses.<br>
<br>
The underlying principle is transferring the risk to a third party (the 
underwriter). Risk transfer can be a misleading concept as it does not really 
transfer the actual risk to the insurer but the economic impact and timing of 
the risk. It may be a disastrous approach should an organisation rely solely on 
risk transfer rather than risk mitigation. Nevertheless, insurance helps in 
limiting losses should an adverse event take place.<br>
<br>
A number of insurance policies exist for financial institutions that may combine 
one or more of the following covers:<br>
<br>
• Banker Blanket Bond (BBB): Fidelity cover against dishonesty, fraud, and 
forgery. Cover may include trading losses and physical assets such as money.<br>
• Directors &amp; Officers Liability (D&amp;O): Covers personal assets of officers and 
directors against legal claims arising from negligence claims towards their 
duties.<br>
• Electronic Computer Crime (ECC): Cover may include systems failures, viruses, 
hackers, and data transmission.<br>
• Building and contents: General cover against damage to or theft of fixed 
assets.<br>
<br>
Normally, financial institutions absorb expenses of relatively small losses but 
may claim compensation from insurers where the severity of loss is high. Risk 
managers need be aware that insuring operational risks introduces a new type of 
risk – delay in payment or non-payment by the insurer.<br>
<br>
Operational risk is a vital component of the overall risk management framework 
that is closely linked to strategic management. Banks and other financial 
intermediaries need to manage operational risk as means to add value to the 
business. Shareholders and the market in general no longer see profitability as 
the difference between revenues and costs, but is assessed by analysing the 
inherent risks embedded in the organisation due to business operations.<i><br>
<br>
