<p align="justify"><font size="4" color="#FF0000"><font face="Verdana">Ramadan 
Bonanza</font></font><font face="Verdana" size="1"><br>
<br>
<b>For retailers in the Sultanate, Ramadan’s significance extends beyond its 
religious relevance. For them, the holy month also represents a traditional 
boost in sales as consumers attracted by mega promotions go on a shopping spree 
lapping up everything from caps to cars. Janaki Raghavan wheels the shopping 
cart across key segments</b><br>
<br>
<img border="0" src="im95.jpg" align="left" hspace="5" vspace="2" width="200" height="174">This Ramadan is no different. As usual, consumers in Oman are facing the problem 
of choice as a plethora of promotions and sales are targeted towards them in the 
holy month. Encashing upon this shopping splurge, the business community in the 
country has pulled out all plugs to keep the cash counters ringing during the 
season. Though consumables undoubtedly register the biggest sales increases, 
thanks to the Iftaar dos, there is a massive surge across other segments as 
well. Sectors like automobile, electronics, white goods and personal accessories 
also see a surge in sales.<br>
<br>
“The festive season is traditionally perceived as the most appropriate time to 
acquire new possessions be it a car for the family, gadgets for the house, 
perfumes or jewellery for oneself. A slew of promotions whet this shopping 
appetite resulting in sharp increases in sales volumes. Performance during this 
period becomes critical since it significantly affects the bottomline of most 
businesses,” mentions a leading electronics distributor.<br>
<br>
<b>In top gear</b><br>
With most automotive franchises achieving about 35-40 per cent of their annual 
budgeted volumes during Ramadan, it is hardly surprising that industry players 
compete fiercely with innovative packages in a one-upmanship game much to the 
delight of buyers. This year as usual there are freebees galore.<br>
<br>
“Ramadan season is a very important period for Toyota in Oman. The Toyota 
Ramadan campaign is the most powerful campaign of the year and generates 
tremendous sales,” reveals a spokesperson from the Saud Bahwan Group, 
distributor of Toyota vehicles in Oman. <br>
<br>
Toyota’s range of benefits for buyers includes five years/100,000 kms free 
service for all passenger cars and two years/40,000 kms for all other vehicles 
except Hilux and free insurance for Echo and Corolla models. The franchise also 
claims to offer record low prices across the range and a high value gift with 
every purchase.<br>
<br>
Ford is offering customers a Ford Explorer SUV in a grand raffle plus 40 high 
value gifts in weekly raffles. Free service and maintenance upto five 
years/100,000kms and free insurance for Ford Focus and a free gift with every 
purchase are the other goodies in the festive package. Coupled with the Ramadan 
offer is the Ford ‘Refer &amp; Win’ schemes, which entitles customers to attractive 
gifts by, raffle for referring potential customers. Lexus, Toyota and Ford all 
also offer the regular six-year protection, best resale value and the respective 
privilege cards. <br>
<br>
This season Lexus buyers stand an opportunity to win a the 2005, Lexus LS430 in 
a grand raffle apart from a chance to win 30 Toyota Corollas in daily raffles - 
a raffle common to all Lexus, Toyota and Daihatsu buyers, two years free 
service, low prices, high value gifts and free bluetooth telephone kit with 
select models. <br>
<br>
<img border="0" src="im96.jpg" align="right" hspace="5" vspace="2" width="200" height="147">Daihatsu offers a five-year/100,000 kms free service package, free insurance, 
record low prices and free gifts. <br>
<br>
“Ramadan is certainly the peak business period for Honda as it is for most 
brands. Campaigns and offers are more competitive than ever. At Honda, we 
recognise the importance of the unique festive spirit that is unique to Oman 
during Ramadan and naturally we wish to share in these celebrations by offering 
our customers’ fantastic value and choice, whichever Honda they choose. With 
Ramadan accounting for nearly 40 per cent of our annual sales volume, success is 
critical. The last two years were excellent for Honda, particularly during 
Ramadan when sales volumes exceeded even our expectations. This year is proving 
to be the strongest year in the history of Honda in Oman and we expect our end 
of year results to reflect this achievement,” declares Paul Turner, National 
Sales Manager, Honda. Almost the entire industry unanimously declares that the 
past two Ramadan seasons have been very productive and targets have been 
surpassed<br>
<br>
The Honda Ramadan offer includes, free insurance, free registration, free 
servicing packages (with extended options available), guaranteed high value 
gifts, Scratch ‘n Win gifts incorporating high value electronic items, plus, a 
“Win Your Car Back” raffle draw and Ramadan special interest rates. <br>
<br>
Kia, represented by Reliable International Automotive is tempting customers with 
a daily raffle of RO1000, free insurance, free service upto two years/40,000 kms, 
50 per cent assured buy back, six -year protection and a high value gift for 
every buyer.<br>
<br>
This Ramadan, Zubair Automotive brings the ‘Once in a Lifetime’ offer with the 
entire Mitsubishi range at a very special price. Customers buying Mitsubishi 
vehicles during Ramadan under this offer also get offers like 30,000 kms/2 years 
free service as per standard service recommendation, free registration and high 
value gifts with every purchase. Also all Mitsubishi Cars and Pick-ups come with 
a 10-Year Unlimited Mileage Warranty.<br>
<br>
From 1st Oct to 31st Dec, 2004, buyers of GMC and Opel will enjoy unlimited 
mileage warranty upto five years, service up to two years/ 40,000 kms, gifts 
with every purchase including a home theatre, TV or portable LCD, DVD players, 
24 hrs road-side assistance, lowest finance rates and best insurance rates.<br>
<br>
Skoda’s festive bouquet is a three years/45,000km free service, 55 per cent buy 
back guarantee for vehicles logging three years/100,000 kms, free registration 
and road side assistance, free gift, AAA membership and guaranteed mobility when 
the vehicle is in the workshop for over 24 hours. <br>
<br>
Much as Ramadan signifies soaring sales graphs it also denotes soaring 
promotional budgets. Distributors go the whole hog, earmarking a good 35-40 per 
cent of their annual budgets for this phase. “Naturally, we utilise a larger 
percentage of our budget for advertising and promotion during Ramadan. Hard 
hitting campaigns and originality is the core publicity strategy at Honda,” says 
Turner reflecting the general trend. <br>
<br>
Timothy Wilmor, Sales Manager – GMC/Opel division, Moosa Abdul Rahman states, 
“We aim at a maximum reach with a judicious mix of different media – print, 
flyers, road shows, e-mails etc”’<br>
<br>
<b>Hot buys for the home</b><br>
The consumer durables segment also achieves high sales target during Ramadan. 
‘Ramadan is a season of brisk sales especially in the home appliances segments. 
Families tend to invest in kitchen /cooking related appliances. Cooking ranges, 
microwave ovens, refrigerators, freezers, small kitchen appliances etc move 
well. There is also a considerable off take in other electronics and appliances 
since renovating houses also becomes a top priority during this time. All in all 
there is definite upswing in the secondary sales. Almost 30 per cent to 35 per 
cent of the annual business is done during this festive period. “We witnessed 
similar trends in 2002 and 2003,” reveals P V Rao, Divisional Manager- EHA, 
Genetco.<br>
<br>
Genetco’s schemes include a discount of up to 45 per cent on products ranging 
from entertainment electronics to office automation products to gadgets. On 
offer are also zero per cent interest, free delivery and installation and a 
special bargain counter.<br>
<br>
Panasonic is enticing customers with a grand sale that offers up to 75 per cent 
off on over 600 items in all its showrooms across the country, till the end of 
Ramadan.<br>
<br>
Mustafa Sultan Electronics will be roping in business with a grand sale. “We are 
also giving out 101 prizes during the sale period including a first prize of a 
42&quot; Plasma TV, a washing machine and refrigerator as the 2nd and 3rd prizes. 
During the month of Ramadan we also have offers on small domestic appliances 
from Philips where we are giving Knorr products free of cost with blenders, 
mixers, deep fat fryers etc,” discloses Sanjeev Nagpal, General Manager.<br>
<br>
Sharp’s Ramadan Promotion comprises a sale plus a coupon on every purchase of 
RO10 which will be raffled giving customers an opportunity to win home-theatre 
systems, 29” colour televisions, DVD Music systems etc. <br>
<br>
In addition, large capacity Sharp refrigerators have a Sharp microwave oven or a 
mini music system thrown in free. Sharp’s Digital Photocopier, which comes at a 
special price of RO385, has a free plain paper fax worth RO54 and a desktop 
calculator worth RO5 as add-ons.<br>
Souq Technia that retails a whole range of renowned consumer durable brands is 
offering value addition to customers in the form of special prices on many 
products plus free gifts for all home appliances.<br>
<br>
LG’s promotion entitles every purchase of RO25 for a raffle draw giving 
customers an opportunity to win one 42” Plasma with speakers and stand, 10 side 
by side refrigerators and 30 Artcool split air conditioners.<br>
<br>
The Iftaar parties and large scale entertaining also become reasons to acquire 
new furniture. “Pre-Ramadan and Ramadan constitute our major business period. We 
are extending very special prices for certain items during this season,” 
elucidates C.M. Sardar, Group General Manager, Saden LLC, a reputed furniture 
outlet.<br>
<br>
Much like the automotive segment, distributors of electronics and white goods 
admit to having aggressive advertising and promotional plans. “We have released 
a colourful 12 page festival catalogue covering the wide range of brands and 
products handled by Genetco. This will act as a reference for the consumers. 
More than 120,000 of these will be distributed across the entire market. A 
comprehensive media plan for advertising has also been drawn out covering 
popular dailies and magazines. Almost 35 per cent of the annual promotional 
budgets will be spent during the season. In addition there will be attractive 
dealer schemes based on criteria of turnovers and growth,” mentions Rao.<br>
<br>
George Alexander, Divisional Manager-Electronics, OMASCO, distributors for 
Panasonic says, “Till last year our publicity comprised of print campaigns and 
flyers but this year in addition to these we have taken the FM route and we are 
advertising on Electronic Billboards also.”<br>
<br>
<b>Fund raising</b><br>
With retail business being upbeat it is but natural that those who are financing 
all these buys are also sporting broad smiles. “Retail Business is at its peak 
during the Holy month of Ramadan as 30 per cent of our annual business in the 
retail segment is achieved during that period,” claims V. Ramakrishnan, 
Operations Manager, National Finance Company (NFC). <br>
<br>
Al Omaniya Financial Services (AOFS) has a tie-up with all the major automobile 
and electronic distributors ensuring attractive rates for the customers. “We 
will be working extended hours to facilitate quick approvals,” says Aftab Patel, 
CEO, AOFS.<br>
<br>
“Existing customers of NFC with a good track record are contacted regularly and 
we have introduced schemes with competitive interest rate for these customers. 
Personalised services to customers during the period of Ramadan have been 
strengthened by deputing specialised staff to dealers showroom for facilitating 
better interaction with the customers and prompt, efficient and quick services 
to the customers. Special schemes for the period have been communicated to the 
dealers which provide flexibility in pricing and credit approval norms,” says 
Ramakrishnan of NFC’s strategy.<br>
<br>
“Ramadan being the crucial and demanding period for dealers it becomes 
imperative for them to have instant access and reach to finance companies. Since 
we have the strongest network in the industry we are well positioned to address 
their needs promptly and at their doorsteps. All our schemes are dovetailed with 
those of the dealers and we will be working extended hours to ensure speedy 
clearances,” declares Raphael Parambi, CEO, Muscat Finance Company Ltd. <br>
<br>
Bank Muscat’s ‘Swipe &amp; Wipe’ scheme promises a win back of RO10,000 for 82 
customers. For every payment of RO50 made, using the bank’s credit or debit 
card, customers stand a chance of winning back everything they spend from 10th 
October to 12th November, in a draw. <br>
<br>
Oman International Bank (OIB) will be holding a grand draw for October during 
the holy month with a grand prize of RO100,000 in addition to the ‘every 10 
days’ draw with one prize of RO25,000 and seven prizes of RO 1000. OIB also has 
sponsorship programmes in various hotels and will be sponsoring give-aways to 
children on ‘Qarn Qashoo’ day in shopping malls<br>
<br>
<b>Gifting ideas</b><br>
With Eid being the time to revamp one’s wardrobe, sales are driven by bulk 
purchases for gifting purposes. “In fact, Ramadan is the bulk sales season for 
the perfumery business. Demand is particularly high at this time of the year for 
gift items such as jewellery, sunglasses, lighters, cigarette cases and 
cufflinks,” mentions a retailer of personal accessories. <br>
<br>
Khimji's Megastore that retails premium brands of perfumes, sunglasses, writing 
instruments, cameras etc is holding a mega sale during November to mark the 
festive season . “This is indeed our high season as a significant share of our 
retail business is achieved during this period. Customers can avail of some of 
the best brands like Nikon, Rayban, Cross, Paco Rabanne, Nina Ricci, Swarovski, 
etc at attractive prices. Discounts range from 20 to 80 per cent on select 
brands,” says Rohit Shah, Assistant Divisional Manager.<br>
<br>
<b>Mall mania </b><br>
Shopping malls drawing customers in droves are the other happening spots. Markaz 
al Bahja has a ‘Scratch &amp; Win’ scheme, which entitles every purchase of RO5 to a 
coupon that offers 24,000 instant prizes. While the grand prize is a Ford Crown 
Victoria, there is a Ford Focus and Ford Fiesta awaiting the 2nd and 3rd prize 
winners. DVD’s and Home Theatres are also up for grabs. About 20,000 customers 
stand to win the recently launched ‘On Demand Card’ which is a loyalty card 
extending discounts ranging from 5-40 per cent in all the 58 outlets in the 
mall. <br>
<br>
“The hot buys of this season are clothes and accessories and we achieve 20 per 
cent of our annual targets in this season. Last year we exceeded our 
expectations and this year we are hoping for a repeat performance. We will be 
concentrating on penetrating the neighbouring belts with posters, leaflets etc. 
We also have a neat print campaign in place. We will be spending 25 per cent of 
our annual publicity budget of RO100,000 for this promotion,” reveals John Jose, 
General Manager, Markaz Al Bahja. <br>
<br>
Sabco Commercial Centre’s Grand Festive Promotion which runs from 15th October 
to 14th February next year offers a chance to win two C180 Mercedes cars and 
four J class round trip tickets to any Gulf Air destination in a raffle of 
coupons given to customers for purchases of RO10 made in the mall on a single 
day. Customers can also look forward to taking home watches, electronics and 
hotel vouchers for a weekend stay for a couple. “The footfall which is usually 
about 3000 customers on an average rises to 5000 in the last 10 days of Ramadan 
fuelled by a buying frenzy,Åh mentions Akbar Ahmed Hussein, General Manager 
adding Åg Most of our outlets will have a sale with discounts ranging from 30 
_60 per centÅh. <br>
