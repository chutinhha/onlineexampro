            <img border="0" src="im36.jpg" align="left" width="250" height="147"><b>What 
            is your leadership style? </b><br>
            My style has a touch of all three – hands-on, participative, and 
            delegative. Hands-on, but not authoritarian, on key issues such as 
            policy; participative on problem-solving, brainstorming and new 
            initiatives; and delegative for HR development and succession 
            planning.<br>
            <br>
            <b>What factors have helped shape your leadership style? </b><br>
            Work experience, education and training. At work, where I have 
            witnessed conflicts being resolved through leadership and vision; 
            through the education process, reading and learning from the 
            management gurus—(Peter) Drucker, (Jack) Welch etc.—and through 
            training to look at corporate and individual goal congruence as 
            integral to fulfilment of objectives. <br>
            <br>
            <b>What are you doing to develop leadership within your 
            organisation?</b><br>
            Creating platforms at functional levels and encouraging staff to 
            speak out and rewarding all contributions that convert to corporate 
            policy. <br>
            <br>
            <b>What is your communication strategy as a leader? </b><br>
            My communication strategy is to keep the team fully informed of 
            everything that is happening within the organisation and in the 
            marketplace. <br>
            And likewise, the customer has to be kept abreast of developments 
            both in the environment and changes within the organisation.<br>
            <br>
            <b>How have you evolved as a leader in the last five years? What is 
            the greatest lessons you’ve learned as a leader?</b><br>
            I have learnt to be patient and view situations from more 
            perspectives. I have learnt that no matter how high the mountain 
            might be, by a calculated assessment of the route to be followed, 
            any height can be scaled. The greatest lesson that I have learnt is: 
            Plan for the worst and hope for the best.<br>
            <br>
            <b>As a leader, tell us about your views on community involvement.</b><br>
            As a responsible member of society, I believe that we must give back 
            to society in some measure what we take from it. The biggest 
            challenge facing the Oman government today is to develop the 
            national manpower resource. At Bahwan Travels, we have developed an 
            integrated Travel and Tourism Course and it is our privilege to now 
            be given the responsibility by the Ministry of Manpower for the 
            second batch of 28 students who are undergoing training.In my 
            personal capacity, I teach the third-year undergraduate students of 
            Tourism at the Sultan Qaboos University. <br>
            <br>
            <b>If you could teach one thing about ‘leadership that drives 
            growth’ to new employees, what would it be?</b><br>
            I would recount the story of Robert Bruce who, after the epic Battle 
            of Banockburn, sat watching a spider trying and failing to spin a 
            web time and again till it eventually succeeded, and reiterate – If 
            at first you don’t succeed, try again!<br>
            <br>
            <b>How do you align individuals’ expectations with your 
            organisational strategy?</b><br>
            Through the process of goal-setting, whereby every individual owns 
            his responsibility and works towards the final convergence with 
            corporate strategy.<br>
            <br>
            <b>When selecting someone to join your team, what qualities will you 
            seek?</b><br>
            Clarity of communication, integrity and motivation. <br>
            <br>
            <b>Do you enjoy taking risks?</b><br>
            Yes, but only cautiously optimistic risks. In my position, I need to 
            take risks everyday. Risks of hedging foreign exchange exposures, of 
            going out to a new marketplace, risks of the uncertain future etc. 
            These risks are integral to the growth potential of my company and 
            were it not for these risks, the organisation would not be where it 
            is today.<br>
            <br>
            <b>What makes you passionate about your company?</b><br>
