<p align="justify"><font size="4" color="#FF0000"><font face="Verdana">King of 
the road</font></font><font face="Verdana" size="1"><br>
<br>
<b>That’s what one feels like when behind the wheels of the new Toyota Prado 
2005, writes Vicky Kapur<br>
</b><br>
<img border="0" src="im88.jpg" align="left" hspace="5" width="250" height="132">There 
is, perhaps, an overabundance of sport-utility vehicles on Omani roads, but the 
Toyota Prado definitely remains the top contender when it comes to luxury and 
off-road capability, and not necessarily in that order. This is one of only a 
few truly elite off-road vehicles. <br>
<br>
Few other vehicles offer the off-road capability of the Prado, and fewer still 
combine this with its level of luxury. If this doesn’t do it for you, perchance 
nothing will. The Toyota Prado can transport up to 10 passengers over the 
toughest terrain, but it’s also a comfortable long-distance cruiser. Inside, 
you’ll find all of the features normally associated with top-level luxury 
sedans, including leather upholstery, power sunroof, automatic climate control, 
and every power accessory under the sun. <br>
<br>
It comes with Toyota’s impressive level of quality control, ensuring excellent 
durability and reliability. Beginning at a price-range of RO8950 for the 
2.7-litre GX 10 PWR variant, the new Prado comes in eight variants, with the 
top-of-the-line Prado VX 8 S.Spl AT setting you back by RO15,150. The Toyota 
Land Cruiser Prado, backed by the inimitable service and network of the Saud 
Bahwan Group in Oman, definitely maintains its value over the long haul with 
minimal depreciation.<br>
<br>
The 2005 Prado comes with just about everything, including leather seating, 
remote keyless entry, one-touch up/down power windows, and an in-dash CD player. 
Sophisticated active safety measures make the Prado easier to control in 
emergency manoeuvres. Standard active safety features include full-time 
four-wheel drive, anti-lock brakes (ABS) with vehicle stability and control. 
Four-wheel drive also enhances the Prado’s off-road capability.<br>
<br>
<b>The outdoors</b><br>
A glance at the Prado will let you see that this SUV’s design is one of 
cultivated restraint. The 17-inch alloy wheels punctuate a muscular simplicity. 
A refreshed front fascia and tail lamps enhance its classic, functional lines.
<br>
<br>
Toyota’s new Prado has a rugged air of purpose that makes it clear it was 
designed for more adventurous pursuits than just mounting kerbs in Muscat’s 
steep roads. The Prado has a smooth body, wraparound front lights, bubble-style 
tail lights, and comes with tinted windows. The improved aesthetics come with 
upgrades to the Prado’s driving dynamics and refinement to boost its appeal in a 
market cluttered with SUVs that claim to offer the best of the on and off-road 
worlds.<br>
<br>
The latest Prado retains its body-on-ladder-frame design—bucking the modern SUV 
trend of monocoque construction—but the chassis is said to be 60 per cent 
stiffer with a lower centre of gravity and a wider track. <br>
<br>
New and attractive side-strips have been adopted on all applicable models. The 
GX10PWR and VX8 come with unique design red/gold stripes while the top-end GX8 
Safari Dsl, GX8 Safari, GX8 Safari SPL, VX8 SPL and the VX8S.SPL are 
differentiated with a rich-look grey/gold stripe. The new, redesigned semi-hard 
type spare-wheel cover has been introduced in the 2005 models. Further, an 
all-new chrome muffler-cutter has been provided to impart a sporty look on GX-R 
8 SPL, VX 8 SPL and VX 8 S.SPL variants. <br>
<br>
<b>The indoors</b><br>
The Prado is ‘big’ inside. The leather seats, which come standard, are firm, 
supportive, and nicely power-adjustable. The 8-way power driver’s seat, 
including power lumbar support, and the 4-way power front passenger’s seat offer 
precise adjustability for personalising comfort. While the front seats offer a 
commanding view of the road nevertheless, a seat lifter on the driver’s seat 
provides the driver with a high eye-point.<br>
<br>
The fenders are easily visible, which is a benefit when picking your way along a 
narrow mountain trail. Getting up to that commanding view entails more of a 
climb than it does in a Ford Expedition or Chevrolet Tahoe. That’s the price of 
a ground clearance of nearly 10 inches, but it’s well worth it when driving off 
road. Large door openings assist getting in and out. <br>
<br>
There’s plenty of legroom in the second row and matches the interior room of 
other full-size domestic sport-utilities. The third-row seat is a bit cramped – 
it’s mainly useful for children. The front and the second-row seats can be laid 
fully flat. A one-touch tumble mechanism on the 60/40 split second-row seats 
enables you to fold them up quickly, giving either access to the third-row seats 
or creating luggage space. To create extra space, the third-row seats can be 
folded up to the side or removed. <br>
<br>
The interior looks contemporary with a dashboard design that makes you believe 
you are inside a cockpit. The graphic display of the field monitor in the centre 
of the dashboard provides a range of useful information. A clock, outside 
temperature, and a compass are displayed at all times. It also displays the 
current and fuel economy average, average speed, potential cruising time, 
driving time, altitude, and atmospheric pressure. <br>
<br>
Storage space abounds for odds and ends. Compartments include a large glove box, 
cup-holders in each row of seats, a cubby in the front console, and front and 
rear door pockets. An overhead console provides three storage boxes plus a 
compartment for sunglasses or a garage door opener. The lid of the large centre 
console box features an 8-position slide function for tailoring its use as an 
armrest. Separate compartments in back hold tools, a jack and a first-aid kit.
<br>
<br>
<b>Performance</b><br>
Toyota’s V6 engine is powerful and quiet. It delivers good performance for 
passing manoeuvres. <br>
<br>
The Prado’s top-of-the-line 4-litre V6 VVT-i (Variable Valve Timing-intelligent) 
engine that OER test drove generates 262 horsepower (net) and, even more 
significant, 38.1 kg-m of torque. Torque is the low-end thrust that gets you 
moving when the light turns green. This torque helps give the Prado a towing 
capacity of 3,200 kg; the full-sized domestic SUVs offer higher tow ratings, but 
3,200 kg is a hefty trailer. <br>
<br>
The Prado is available in a choice of petrol or diesel engines. Toyota has 
introduced a new and powerful 2.7-litre petrol engine for the GX gasoline models 
to exceed customer expectations in terms of engine power and acceleration. This 
engine is a 4-cylinder, in-line, 16-valve DOHC, Efi, VVT-I engine that delivers 
160 HP (net) at 5200 rpm. There is also a choice of the mighty 4-litre (262 HP) 
petrol engine that delivers responsive power and sporty driving, and the 
efficient 3-litre diesel engine. <br>
<br>
Ride quality is smooth and supple on the road. Interior noise is relatively low. 
The TEMS (Toyota Electronic Modulated Suspension) closely controls the damping 
force of the shock absorbers to provide a stable body position and smooth ride 
comfort. The suspension adds to the Prado’s luxury lineage by providing several 
options for rear height control to clear rough terrain. An auto-levelling 
function on the electronically-controlled rear air suspension keeps the vehicle 
height constant for stable driving even as passenger numbers and loads change.<br>
<br>
Superior 6-speed manual and 5-speed automatic transmissions instead of the 
earlier 5-speed manual and 4-speed automatic transmissions on the VX grades 
deliver improved vehicle response and efficiency. Venture off the highway and 
the Prado can take you just about anywhere. Only Land Rovers and Jeeps can 
compete in terms of off-road capability. <br>
Toyota designed the Prado to take a lot more punishment than most owners will 
ever dish out. Ground clearance is among the best in the class at nearly 10 
inches.<br>
<br>
Toyota’s full-time four-wheel-drive system is capable of splitting torque evenly 
front to rear, with the rear wheels turning in unison while the front wheels 
bias torque to the side with the best traction. To achieve this, simply turn the 
selector switch to full-lock mode and shift the two-speed transfer case into 
low-range four-wheel drive. <br>
<br>
<b>Safety</b><br>
The Prado’s ABS senses the slope and roughness of the terrain and reduces the 
anti-lock influence over the brakes; this improves stopping performance off road 
and avoids early activation on steep slopes. Several technologies come standard 
that improve the driver’s ability to control the Prado in emergency manoeuvres.
<br>
<br>
The Prado comes standard with ABS, which allow the driver to maintain steering 
control of the vehicle in an emergency-braking situation. All that technology, 
along with the Prado’s excellent braking performance, results in a vehicle 
that’s prepared to handle an emergency-stopping crisis. <br>
<br>
The Prado’s unique torque sensing TORSEN limited slip centre differential (LSD) 
with locking function distributes a greater amount of torque to either the front 
wheels or rear wheels, depending on which has the most traction. When engaged, 
the locking feature eliminates the front-to-rear power adjustment for confident 
footing in slippery or more difficult road conditions. <br>
<br>
<b>The finishing line</b><br>
Few sport-utilities offer more off-road capability and none offer higher 
all-around quality than the 2005 Toyota Land Cruiser Prado. So, while you may 
pay a premium price, you’ll get a premium vehicle.<br>
<br>
<i>Road test courtesy Saud Bahwan Automotive, the sole dealers of Toyota 
vehicles in Oman.</i></font></p>

