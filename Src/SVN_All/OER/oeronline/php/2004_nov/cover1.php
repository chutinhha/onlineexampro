            ‘‘You can turn into a leader today’’<br>
            </font><font face="Verdana" size="1"><br>
            </font><b><font face="Verdana" size="2">Irshad Lawati, <br>
            Managing Director, Shell Oman Marketing</font></b></p>
            <p align="justify"><font face="Verdana" size="1"><b>
            <img border="0" src="im30.jpg" align="left" width="250" height="133">What 
            is your leadership style?</b><br>
            I suppose it would always be difficult to have a straight 
            authoritarian, participative or delegative style of leadership in 
            someone. In my opinion, my leadership style is more participative 
            and then delegative, than authoritative – except on matters of 
            principles and ethics. This, probably, also depicts the 
            organisational culture of my company. There are too many actual and 
            potential variables in real life that demand flexibility and 
            adaptation from a leader, and cause a shift in style to one or the 
            other of the prevalent styles, dependant on the situation. To make 
            it sound too simplistic, imagine the different kind of relationships 
            (or styles) one ought to have with his father, wife, son and 
            brother.<br>
            <br>
            <b>Which of the following factors do you think have helped shape 
            your leadership style – family, tribe, religion, work experience, 
            education and training, other external influences, or any 
            individual? </b><br>
            In one word: all, in a greater or smaller way. But I believe the 
            organisational culture has played a big role in my case in the 
            current job. On the other hand, I guess one also needs a fair amount 
            of flexibility to adapt to situations. A successful leader should be 
            able to enter a conflict and bring things into harmony. On the 
            values part, I’d probably be very ‘authoritarian’. In such a 
            process, your whole list of leadership-shaping factors gets involved 
            – ethics from the religion, heritage from the family, tactics theory 
            and practice from education, as well as do’s &amp; dont’s and can’s &amp; 
            and cant’s from the organisational culture. To a believer, I suppose 
            the religion becomes the filter of acceptability of traits that are 
            instilled by other qualitative doctrines and heritages forming the 
            style, combined with the blend of technical knowledge and skills. In 
            my case, my religious beliefs as a Muslim are definitely at the core 
            of it.<br>
            <br>
            <b>Can you provide an example of a leadership challenge you faced 
            and how you solved it?</b><br>
            I project managed an acquisition venture involving the takeover of a 
            well-established organisation with a structure based on certain 
            values which were different from those of the new organisation. A 
            rushed amalgamation of the two parts could have resulted into a 
            morale collapse.<br>
            The integration task demanded crystal clear understanding of: The 
            new values and vision; the old value base and the gap; and tolerance 
            levels, scenarios and feasible response options. The final result 
            was a smooth migration of all business activities with absolutely no 
            losses.<br>
            <br>
            <b>What is your communication strategy as a leader? </b><br>
            I believe that communication is equal to the function of the brain 
            in the human body – a brain-dead human being is effectively a dead 
            one! When it comes to the organisations, effective functioning 
            depends on effective communication. <br>
            <br>
            <b>How have you evolved as a leader in the last five years? What are 
            the greatest lessons you’ve learned as a leader ?</b><br>
            I think both depth and breadth of experience are key in shaping up 
            leadership skills. The biggest lesson for me has been the 
            realisation that heading a ‘blue-chip’ organisation is not the limit 
            and it is vital for leadership aspirants to keep shifting their 
            horizons to newer heights every time a growth milestone is realised.
            <br>
            <br>
            <b>If you could teach one thing about ‘leadership that drives 
            growth’ to a group of new employees, what would it be?</b><br>
            This would be the ‘customer-oriented’ mindset of adding value, which 
            is based on the market-led approach. Nothing in business is more 
            compelling than understanding what your customer wants – this leads 
            to adapting your offering to the customer. <br>
            <br>
            <b>What strengths do you rely on most in your daily life as a 
            leader?</b><br>
            Listening, motivation and ‘pushback’ are essential. These lead to 
            appreciation of situation, like getting a feel of the house, 
            acknowledging the effort that has gone into building it, and making 
            sure that walls are not falling apart, while constructively 
            criticising the flaws in the structure.<br>
            <br>
            <b>What gives you professional satisfaction?</b><br>
            I get a lot of satisfaction from the experience I have obtained in 
            doing different jobs ranging from government jobs, agricultural 
            requisites, personal computers, banking, petroleum products, project 
            management, sales and marketing roles. This also helps me in my 
            current role.<br>
            <br>
            <b>What makes you passionate about your company? </b><br>
            The values of my company are probably the most important part of it. 
            Shell Oman adopts the Shell Group’s policies as well as the values 
            of Oman, and that forms the baseline of its culture. Some of the key 
            aspects would be: consistent policies, clear business principles, 
            respect for people and recognition of achievement.<br>
            <br>
            <b>What advice would you give those who want to be the leaders of 
            the future?</b><br>
            First of all, don’t wait for the future – you can turn into a leader 
            today. It is much more the mindset and attitude to your role, than 
            your position in the organisation (company, group, team, etc.) or 
            the authority level. Leaders are not born, they are ‘created’, and 
            mostly by their own conduct. I guess reading the stories and 
            biographies of great leaders helps in this regard. The most 
            interesting definition I have seen for leadership is that it is a 
            ‘‘skill of influencing people to work enthusiastically toward goals 
            identified as being for the common good.”</font></p>
