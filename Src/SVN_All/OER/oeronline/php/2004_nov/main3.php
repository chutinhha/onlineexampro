            <img border="0" src="im72a.jpg" align="left" width="100" height="194">In 
            spite of winning one of the most competitive deals that the world 
            has ever seen, Nawras Telecom Consortium has its work cut out – 
            succeed in a market where the mobile service penetration rate is 
            roughly 25 per cent as Oman remains one of the least-connected 
            nations in the Gulf Cooperation Council (GCC). <br>
            <br>
            The task of providing a complete network in the mountainous terrain 
            is arduous, not to mention the competition with the already well 
            entrenched Oman Mobile, but that does not stop Team Nawras from 
            enthusing an infectious air of energy and optimism! <br>
            <br>
            Nawras, the victorious consortium, made up of Qatar Telecom (Qtel), 
            the Danish-owned TDC and Omani partners represents a major step 
            forward in the liberalisation of Oman’s telecommunications industry, 
            according to CEO, Ross Cormack.<br>
            <br>
            So what swung the deal in Nawras’ favour in a field of 28 
            competitors that included Vodafone and Orange? “We already had a 
            joint venture between Qtel and TDC, which was specifically focussed 
            on developments in Middle East, North Africa and in East Europe. The 
            skills that these parents brought were very relevant to Oman because 
            Qtel brought the cultural understanding and an understanding of the 
            Omani market. It was also to do with our ability to roll out new 
            networks in new countries and that is where our experience with TDC 
            comes into play,” explains Cormack. <br>
            <br>
            The starting point for Nawras’ bid was based on finding a way out to 
            best serve the Omani customer. Cormack says, “The Omani customer is 
            demanding. It is up to us to listen to what he wants and provide him 
            with products and services which are relevant, easy to use and add 
            value to his life.” Having said that, the challenge lies in tapping 
            customers who have never used a mobile service before. “We have to 
            understand what has been stopping them from using the service. We 
            have to offer service in a manner that is attractive and meets their 
            needs,” adds Cormack.<br>
            <br>
            <img border="0" src="im72b.jpg" align="right" hspace="5" width="100" height="181">Reiterating 
            the fact that Nawras wants to do things “in a pleasingly different 
            way and be the mobile communications provider of choice,” Cormack is 
            still keeping his cards close the chest on the launch date of the 
            service. <br>
            <br>
            “It will have to be in the early part of 2005. We are going flat out 
            on building our services and company. As far as infrastructure goes 
            we are getting prepared to roll out the first 170 plus radio-based 
            stations. This will allow us to get going as fast as we possibly 
            can. We have also agreed to a backbone contract with Omantel to 
            provide the communications across our networks.”<br>
            <br>
            Around 30 per cent of Nawras is owned by Omani funds, mostly pension 
            funds. The board has an Omani chairman and representatives from the 
            Omani pension funds, Qtel and TDC. An IPO of Nawras is also on the 
            cards within the first five years of operations. The timing of IPO 
            will depend on a favourable financial market condition. “We have to 
            build a profitable company first as we cannot go for IPO unless we 
            have real customer and real revenue and have covered our costs 
            first,” cautions Cormack.<br>
            <br>
            On meeting Omanisation targets, Cormack says, “We have very 
            challenging Omanisation targets. And that is why we have to be very 
            focussed as how to get our training right. The training programmes 
            are being developed. Some people will be trained at Qtel and some at 
            TDC, to get a first hand idea of how those companies operate, deal 
            with customers and build propositions. Because of tertiary 
            employment in fields like advertising, suppliers, dealers etc, 
            probably 1200 jobs will be created.”<br>
            <br>
            But then what is Nawras’ cutting edge? What are they bringing new to 
            the market? Special Advisor to Nawras, Abdullah Al Rawahi answers, 
            “Nawras is the new kid on the block with its own bag of tricks and 
            experience to make a splash in the market! Oman Mobile provides good 
            coverage but there is room for improvement in the customer services 
            front. The presence of Nawras will bring the much-needed competitive 
            environment where the customer will have a big say.”<br>
            <br>
            Ross Cormack sums up Nawras’ USP succulently, “The process of 
            getting success is very clear–by listening to customers. What we 
            found out was that customers want excellent service. Basics like 
            when they want to make a call and hold on as long as they want to. 
            They want to switch it off when they want it and not on the will of 
            the network.” Billing has also been an area of concern and for that 
            Nawras will be making use of the billing systems used by Qtel and 
            which apparently are the best in the business. <br>
            <br>
            <img border="0" src="im73.jpg" align="left" hspace="5" width="100" height="175">“Both 
            Qtel and TDC have had hands-on experience in providing world quality 
            billing systems,” adds Cormack. While Cormack maintains that it is 
            too early to give details of the services and even the specific 
            outlets Nawras has signed off lease contracts for the fitting out of 
            the first outlets. Nawras is also keeping its plan of the Third 
            Generation (3G) services under wraps. The Chief Technical officer, 
            Peter Rubeck points out that, “What we are concerned at this point 
            of time is building a service that works effectively. We have to get 
            the basics right first. What the Omani customer can expect in terms 
            of service is innovation.”<br>
            <br>
            Rubeck points out that the challenge of setting up operations in 
            Oman will be very similar to other start-ups that he has worked on 
            in other part of the world. “We have received great cooperation and 
            backing from Omantel and with the kind of commitment and energy that 
            my technical team is showing we should be on a roll very soon.”<br>
            <br>
