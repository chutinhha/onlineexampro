            <font size="4" color="#FF0000"><font face="Verdana">Innovative 
            Leadership Development<br>
            </font></font><font face="Verdana" size="1"><br>
            <b>A presentation and discussion at a recent MCE HR Conference in 
            Lisbon set out to look at fresh research and new thinking 
            influencing organisational strategies and practices for developing 
            their leadership talent. A few of the key slides from the 
            presentation are included in this article by Penny Albertella, MCE 
            Faculty Member</b><br>
            <br>
            <img border="0" src="im46.jpg" align="left" width="200" height="250">The 
            need for more flexible and innovative approaches is being driven by 
            dramatic changes in the workplace and the fact that the challenges 
            facing leaders are more daunting than ever. Technology, the 
            Internet, downsizing, industry consolidation, globalisation and 
            innovation are just a few of the trends creating the flatter, 
            leaner, and more adaptive organisations and the subsequent need for 
            innovative leadership. <br>
            <br>
            The leadership model developed by the author (see below), builds on 
            the work of Adair, and shows the multiple talents required by the 
            leader of today and tomorrow. <br>
            <br>
            The addition of virtual working, the need to create the environment, 
            structures and processes whereby innovation happens, to understand, 
            celebrate and embrace the diversity offered by culture are key 
            attributes and skills urgently required by the global leader of 
            today and tomorrow. <br>
            <br>
            In addition, competition for these exceptional leaders is hotting 
            up! Research undertaken by McKinsey, The war for Talent, 
            hypothesises that an economic growth rate of 2 per cent per annum 
            for 15 years will actually increase demand for ‘executives’ by 
            around one-third. The supply of people in these categories is 
            actually declining both in the US and the EU. <br>
            <br>
            Organisations need to become increasingly innovative to become 
            talent magnets. A survey by Career Innovation asked what would most 
            affect an employee's decision to stay or go. Here are the top six 
            items reported: <br>
            • Career development; responsibility; promotion <br>
            • Competitive/appropriate salary/ benefits <br>
            • International opportunities <br>
            • Learning, personal growth, development <br>
            • New assignment; job expansion; challenge; variety <br>
            • Lifestyle; spouse plans; family; travel; stress; hours </font></p>
            <p align="justify">
            <font face="Verdana" size="1">The new generation of emerging leaders 
            have very different aspirations from those of the current generation 
            of leaders. Emerging leaders are asking for leadership development 
            at an earlier age rather than just skills training. They report that 
            there is little leadership training available to them. Development 
            is not organised and structured enough for them. <br>
            Current executives in 50 large US companies were involved in a piece 
            of research by McKinsey to identify the types of development they 
            had undertaken and the comparative value they placed on them. The 
            results (see below) showed that traditional ‘programmes’ of study 
            were seen as the least effective. These are the programmes that 
            focus on leadership theory with case studies to put this theory into 
            practice and are delivered by management schools or as in-company 
            programmes. <br>
            <br>
            If you think about the six reasons given earlier as to why someone 
            would stay in the organisation or go, along with the development 
            experiences of our leaders and the needs of the emerging leaders, 
            the message is loud and clear – the ‘programme’ approach alone is of 
            less value to both the leader and the organisation itself. <br>
            <br>
            <img border="0" src="manage.jpg" align="right" hspace="5" width="200" height="144">The most valued and effective approaches to leadership development 
            identified in the executive research include a different approach to 
            job structure, special projects built around current and future 
            business imperatives, speed of job moves, feedback on their 
            strengths and weaknesses (including 360-degree feedback), exposure 
            to good role models and individual coaching. These approaches fall 
            into the remit of what we call ‘stretch learning’. <br>
            <br>
            It could be argued that this approach is used in organisations that 
            have fast-track promotion schemes. However, experience shows that 
            there is often a piecemeal approach taken and job moves are often 
            driven by urgency (or because a particular anniversary has been 
            reached – e.g., an 18-month or 2-year mark) rather than by the 
            important needs of the individual and organisation. The job-role is 
            not often seen as part of the development strategy and, therefore, 
            the structure of the role is not tailored to meet the needs of the 
            organisation and the individual. <br>
            <br>
            Organisations that are less innovative in their approach to 
            leadership development tend to have a number of job roles mapped out 
            in the organisation and high potential leaders are then matched to 
            the job role. Apple turns this on its head; the new starter, or 
            developing leader, is asked to spend some time in the organisation, 
            or a part of the organisation, to get a feel for where they would 
            add most value and where they would get most value for themselves 
            (win/win) – they are then able to give themselves a job title and 
            set to work planning the outcomes they will achieve over a set 
            period of time and will identify any specific development needs they 
            have to help them achieve these outcomes. <br>
            These development needs might be met through coaching, job swap, 
            e-learning or maybe a traditional workshop – the method is the one 
            which best suits the individual and the situation. Arrangements are 
            made for regular feedback and achievement capture. <br>
            <br>
            The organisations that attended the presentation were asked to place 
            themselves on the JPII Scale (see below). Most present were 
            currently a 2 or 3 and when asked where they needed to be to attract 
            and retain talent and to meet the needs of the organisation and its 
            customers, they all needed to move at least one, if not two spaces 
            to the right. <br>
            <br>
            So what is stopping organisations from becoming more innovative and 
            incorporating ‘stretch learning’ for their leaders? In my 
            experience, and taking the experience represented at Lisbon, it 
            comes down to several key factors: <br>
            • Lack of experience and expertise in managing ‘stretch learning’ in 
            the organisation <br>
            • Resistance to this approach from Board Level as it requires 
            complete buy-in and support from the most senior people in the 
            organisation<br>
            • Lack of clear plans to move from where the organisation is now on 
            the JPII Scale to where they<br>
            • Talking to organisations that are already successful at this 
            approach or consultancies that can help you put the case for this 
            approach to the board and help you plan an effective strategy to 
            implementing this over a period of time are key to success. As with 
            any approach, there are pitfalls and strategies that help build 
            success – do not reinvent the wheel! <br>
            <br>
            MCE works with organisations to help them devise leadership 
            development strategies which embrace the best of the programme 
            approach integrated with stretch learning designed to meet their own 
            organisational needs and the needs of the emerging leaders. The 
            model above demonstrates a leadership programme devised for a client 
            organisation. Their current and future business imperatives were 
            used as ‘real-time’ case study material and as project work in the 
            workplace during and after the programme. Each participant devised 
            their own plan around the core programme using any of the options 
            identified and any others they thought appropriate. E-learning and 
            e-facilitation was used to provide further blended solutions.</font></p>
