            others. <br>
            <br>
            The entire book is very well structured and takes you through the 
            process of brand leadership without getting too technical and uses a 
            language that can be understood by laymen and guru’s alike. I 
            particularly liked the use of known brands as examples to convey the 
            chapter content across to the reader. One other impressive item 
            about this book is the “discussion points” at the end of each 
            chapter. A lot of these books try to get the reader to perform 
            “exercises” that test knowledge and absorption of the chapter 
            content, whereas I found the discussion points a lot more relevant 
            and challenging and it did get me thinking about the brands that I 
            represent and how they fit into the market in Oman.<br>
            <br>
