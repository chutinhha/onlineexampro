            rather than as brand owners and therefore the chapter that discusses 
            about the “house of brands” vs the “branded house” is something that 
            all marketing managers in local companies would be well advised to 
            read. Overall this is a book I will refer to over the coming months 
            and the chapter on “sponsoring as a brand benefit” will change the 
            way Oman United Agencies will manage its brands in 2005.<br>
