<img border="0" src="im82.jpg" align="left" hspace="5" width="125" height="147">It is generally understood that mergers of banks (or any companies for that 
matter) are the result of the successful workings of the market, which naturally 
strives for certain benefits such as those from economies of scale and scope, 
increased market share, risk reduction and corporate control. <br>
<br>
A fundamental premise underlying the market for mergers is the existence of a 
positive correlation between the market price of shares of any given company and 
its attractiveness to another company which may wish to acquire it to achieve 
the sought-after outcomes described above. <br>
<br>
While the market’s healthy momentum and efficiencies will lead to corporate 
mergers from time to time, individual shareholders may not always be satisfied 
with the terms of the merger if it does not result, or the shareholders do not 
perceive it to result, in an equitable outcome for them at a personal level. <br>
<br>
<b>Legal Basics of Mergers</b><br>
The Oman Commercial Companies Law (promulgated as Law 4/1974 as amended) 
provides for the merger of companies by way of absorption, involving the 
dissolution of one or more companies and the transfer of its or their assets and 
liabilities to an existing company, which becomes the “surviving” company. This, 
of course, is the method adopted for the proposed merger of National Bank of 
Oman SAOG and BankMuscat SAOG. (Alternatively, companies may merge by way of 
amalgamation, involving the establishment of a new company to which the assets 
and liabilities of the merging companies are transferred). <br>
<br>
The Law provides that, when a merger by absorption is contemplated, the 
“disappearing” company must first pass, at an Extraordinary General Meeting (“EGM”) 
a resolution for its own dissolution and the transfer of its assets and 
liabilities into the surviving company. The net assets of the disappearing 
company must be valued in accordance with the last audited balance sheet to 
ascertain a suitable evaluation of its share capital. <br>
<br>
The surviving company will then pass an EGM resolution for an increase in its 
capital which will reflect the valuation report of the disappearing company, or 
any other negotiated amount which both companies have determined to be fair, and 
will also issue new shares to be distributed proportionately to the shareholders 
of the disappearing company once the merger takes effect.<br>
<br>
In the case of a bank merger, the proposed merger plan must first be submitted 
to the Central Bank of Oman, which will consider the merger from a general 
“national economic interest” perspective. <br>
<br>
The share value, established for the purpose of the share exchange, will be 
considered separately by the Capital Market Authority, which will consider such 
value with a view to ensuring the protection of the shareholders of both 
companies involved in the merger, and preserving general market integrity in the 
transaction.<br>
<br>
<b>Rights of Dissatisfied Shareholders</b><br>
All shareholders will be bound to the terms of the proposed merger because, as 
noted above, such terms will have been ratified by an EGM. However, any 
dissenter shareholder (or any interested third party) who views the terms of the 
merger to be unfair as to the true value of the shares of the disappearing 
company may, within five years, file a lawsuit with the competent Primary Court 
(Commercial Circuit) objecting to the terms of the merger as approved by the EGM 
(It is notable here that, although the law provides for a five year period 
during which a claim may be made, it would be highly impractical to file such a 
case after the merger actually took place). <br>
<br>
After filing such a case, the dissenter may be able to obtain an interim 
injunction to postpone the merger pending any judgement in the dissenter’s case. 
The outcome of the case will depend on various factors including the court’s 
view of the fairness in the evaluation of the share value.The concept of 
fairness has two basic aspects: fair dealing and fair exchange for the 
disappearing company’s shares. <br>
<br>
Fair dealing concerns questions of when the transaction was timed, how it was 
initiated, structured, negotiated, disclosed to the directors, and how the 
approvals of the directors and the shareholders were obtained. <br>
<br>
The latter aspect of fairness relates to the economic and financial 
considerations of the proposed merger, including all relevant factors any other 
elements that affect the intrinsic or inherent value of a company’s capital 
value.<br>
<br>
Underlying the basic concept of value in this context is that the shareholder is 
entitled to a fair exchange for his or her proportionate interest in the 
disappearing company as a going concern. This relates to the true or intrinsic 
value of the dissenting shareholders’ assets, which have been taken as a result 
of the merger. In determining what figure represents this true or intrinsic 
value, the appraiser and the court must take into consideration all factors and 
elements which reasonably might enter into the fixing of fair value. Thus asset 
value, market value, dividends, earnings prospects, and any other facts which 
were known, or which could be ascertained as of the date of the merger and which 
throw any light on future prospects of the merged bank, are pertinent in such an 
enquiry. <br>
From a consideration of the above, the court will be in a position to determine 
whether or not the valuation of the shares, as approved by the EGM, is 
appropriate and truly fair to the shareholders of both companies.<br>
<br>
<b>Conclusion</b><br>
There can be no doubt that mergers generally create value in the market and that 
they represent an opportunity for a positive return for shareholders of both 
companies (although such return is usually perceived on a more immediate basis 
by the acquired company’s shareholders). Nevertheless, the rights of minority 
dissenting shareholders to raise objections to such valuation is an important 
aspect of preserving a responsible and trustworthy marketplace. <br>
