            A good mix of delegative and participative. I always believe in 
            delegating and then being a part of team. My experience proves that 
            one cannot do much without involving all the others who have a stake 
            (interest in the outcome). At the end of the day, important 
            decisions affect every one in an organisation. Delegative, because I 
            believe in giving power to others and make them feel that their 
            decisions are equally important – this brings the best from people 
            as it assures them of belongingness to their work. Delegative and 
            Participative are the best leadership style one can adapt for 
            optimal efficiency and longevity of the organisation and the team.<br>
            <br>
            <b>What factors have helped shape your leadership style? </b><br>
            Family is one major factor followed by religion and education. I 
            always believe that I was fortunate to live outside the country for 
            almost 10 years, which helped me immensely in understanding an 
            international culture. I feel all human beings are equal except for 
            the fact that their approach to life is affected by economic or 
            political factors. Indeed, it was by choice that I did economics, 
            mastered in social studies that opened my mind to a lot of facts on 
            understanding society and people. <br>
            <br>
            <b>Can you provide an example of a leadership challenge you faced 
            and how you solved it?</b><br>
            The major challenge I confronted was when my credibility as a leader 
            was challenged within the management and the board. In life, you 
            cannot expect only ups – there are downs as well. Your leadership 
            success is better told when you transform by leadership the downs 
            and just not manage well during the ups. I worked patiently and I 
            proved myself. <br>
            <br>
            <b>What are you doing today to develop leadership within your 
            organisation?</b><br>
            I am empowering people around me. At OITE, decisions cannot wait for 
            top management – even the lowest of staff members make their 
            decisions.<br>
            <br>
            <b>What is your communication strategy as a leader?</b><br>
            Always listening and allowing others to talk. I believe in giving 
            freedom for others to express their opinion. Our business is 
            successful due to the free flow of information. <br>
            <br>
            <b>How have you evolved as a leader in the last five years? What are 
            the greatest lessons you’ve learned as a leader?</b><br>
            I have always been in the process of learning. I learn from books, 
            from self-experience and experiences of others. In the last five 
            years, I have managed to involve myself in various activities. I 
            achieved a seat as a board member of the Arab Union for 
            International Exhibitions and Conferences to represent the Oman’s 
            event management industry. The greatest lesson I have learnt is that 
            there are huge differences between an organisation. Sharing the 
            success and growth can eliminate these differences. <br>
            <br>
            <b>As a leader, tell us about your views on community involvement.
            </b><br>
            Our organisation thrives on the principle of community development. 
            Every event we organise is for the benefit of the sector we serve. 
            For example, we organise the higher education exhibition by bringing 
            in 130 international universities every year – the community gets 
            direct access to interact and choose the best. <br>
            <br>
