<b>as he is fondly referred to, once marched for the Sultan’s Armed forces, 
taught himself English by reading from news-papers discarded by British 
officers, introduced Pepsi Cola to Oman, loves reading Jeffrey Archer, and had 
to apply for a licence to ride his first bicycle. Saubhagya Mohan Kala meets 
Maqbool Hameed Al-Saleh, the chairman of Oman Holdings International (OHI)</b><br>
<br>
<img border="0" src="profile.jpg" align="left" hspace="5" width="150" height="190">Born 
in Muttrah, life was not a bed of roses for Maqbool Hameed Al-Saleh. ‘‘My 
ancestors had reversed the proverbial rise into a fall, and had taken the family 
fortunes from riches to rags! We were quite poor, and my father had to work in 
public transport just to make the ends meet,’’ he remembers.<br>
<br>
It isn’t surprising, then, that Al- Saleh was not able to get even high-school 
education and found himself in the Sultan’s Armed forces in 1959. Totally at sea 
with English, Maqbool Hameed self-taught himself the language by reading the 
newspapers discarded by the British officers. ‘‘I would have been sent home 
after the first month were it not for the kindness of Sgt. Siddiqui, who got me 
posted in the orderlies’ room, where I could survive,’’ he recalls.<br>
<br>
Having graduated from what he calls ‘‘The School of Hard Knocks,’’ Al- Saleh 
ensured that his siblings and his children do not lose out on the opportunity to 
get quality education. His first son, a graduate from the UK, has joined him in 
the family business. The second son has recently graduated from George 
Washington University in the US and works for HSBC. His youngest daughter has 
joined one of the best universities at Bristol in England after having finished 
her schooling from the Sultan School in Oman.<br>
<br>
<b>Young challenge</b><br>
Maqbool Hameed Al Saleh recounts an intriguing challenge that he faced as a 
youth. This was when the Army was headquartered in Bait Al Falaj. With no public 
transport or personal vehicle at his disposal, Maqbool Hameed had to walk a 
great distance in peak summer. With lack of proper roads, he also had to climb a 
hill to cross Darsait! <br>
<br>
Eventually, tired with this daily trudge, Maqbool Hameed told his father that he 
wanted to buy a cycle. ‘‘I found a second-hand one for just Rs 25 (210 baisa). 
He seemed apprehensive and said that I would need a licence. And incredible as 
it may sound, I did get a cycle licence for my little cycle! To the affluent 
youth of today, this may seem farcical, but it was a big challenge for me to get 
my first cycle.’’</font></p>

