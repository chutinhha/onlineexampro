<p align="justify"><font face="Verdana" size="1">
<b>Career Graph</b><br>
While the army was his first paying job, Maqbool Hameed believes that his 
‘career’ started with the auditing firm Ernst &amp; Young (then Whinney Murray) in 
Dubai. ‘‘The job was one of adding numbers in the telephone directory. I did 
that for some time – having no clue about what I was doing. And thought they 
were mad to pay me Dhs800 for this!’’ he exclaims. <br>
<br>
Totally perplexed with his work profile, Maqbool Hameed to his consternation was 
given a raise, furnished accommodation and transferred to Abu Dhabi! In this 
confused state of mind, he went up to his boss, Trevor Potter, and said that he 
wanted to quit. “He couldn’t fathom why a person who was doing so well wanted to 
leave,” reminisces Al- Saleh. It was only when his boss commented that he was 
‘auditing well’ that Maqbool Hameed realised that ‘‘I was actually auditing, and 
was doing a good job of it too.’’<br>
<br>
Like the prodigal son, Maqbool Hameed returned to Oman when His Majesty Sultan 
Qaboos bin Said assumed power. Making the most of the tiny savings that Maqbool 
Hameed had left over from his Whinney Murray days, he started a small business. 
‘‘I brought a truckload of Pepsi Cola from Dubai and introduced it in Oman. 
Soon, I added more trucks. The rest, as they say, is history.’’ And what a 
history it has been. From its humble beginnings, the OHI Group has today grown 
into a RO35.24 million business monolith, and featured as No. 9 on this year’s 
The OER 20, the annual ranking of the largest listed companies in Oman. “We have 
slowly walked together and got to where we are today. We have not zoomed or 
whizzed to get here, we have walked and climbed: a long walk it was,” says a 
visibly content Maqbool Hameed.<br>
<br>
The OHI group owes its success to diversification, and Maqbool Hameed firmly 
believes that one has to be always on the lookout for opportunities in new 
areas. Always examining options and potential business avenues, Maqbool Hameed 
says, “I do not believe in putting all eggs in one basket, and that is perhaps 
why OHI is so stable today. A decline in one business is offset by a climb 
elsewhere and so on. We always look for ways of adding value and utilising 
resources for growth. This also provides direct benefits to the country, since 
it would provide job opportunities for young Omanis in line with His Majesty’s 
vision and recent directive and earn valuable foreign exchange.’’<br>
<br>
Around the same time he started his Pepsi distribution business, Maqbool Hameed 
met up with two of his acquaintances from the army days, an Omani and an 
Englishman, who asked him to join as a secretary in the Ministry of Diwan. Al 
Saleh agreed, and eventually rose to the position of an undersecretary. “Those 
were probably some of the happiest days of my life. I got an opportunity to get 
close to His Majesty and experience at first-hand his kindness and generosity,’’ 
he says with a beaming face.<br>
<br>
<b>Management Mores</b><br>
Maqbool Hameed Maqbool Hameed’s management philosophy is as practical as his 
approach to life: “Do not go in for showy labels or complex concepts – be 
direct, simple and straightforward, and approach all problems with a spirit of 
cooperation and consensus.’’ This approach applies to all his partners, 
colleagues, employees and clients. <br>
Living by the credo, ‘‘Teamwork, first and foremost,’’ OHI’s strength, according 
to Maqbool Hameed, lies in its people. He remains hands-on when it comes to 
picking and judging the individuals he wants to work with him. ‘‘Once I get the 
right person, I leave him to get on with the job. After all, he has been 
selected for his ability to deliver the goods,’’ he insists.<br>
<br>
A modest Maqbool Hameed adds almost in the same breath that he is not so 
arrogant as to pretend that all his choices have been correct. “Sometimes, I 
have found that particular managers were not as good as I thought them to be, 
but if you see the number of my senior executives with the years of service in 
OHI, you will agree that my misses have been few and my hits many.’’ And many 
more to go! </font></p>

