<p align="justify"><font color="#FF0000" face="Verdana"><font size="4">
Empowering for employment<br>
<br>
</font></font><font face="Verdana" size="1"><b>The State must actively encourage 
students to enter fields of study that are relevant to the job market, argues 
Tarek Abu Asi<br>
</b><br>
<img border="0" src="im94.jpg" align="left" width="100" height="122">A common misconception harboured by many students in the Middle East, and to a 
lesser extent by those in Europe, is that a student will immediately find work 
after graduation. While some students do find jobs straightaway, my research 
reveals that a majority tends to have a waiting period of between four and nine 
months. This is especially true of business graduates.<br>
<br>
Having conducted in-depth interviews on this subject with 20 students, both male 
and female, the results show up interesting and sometimes alarming results. 
Alarming because an unemployed graduate or a graduate employed in fields outside 
his field of specialisation is gaining experience that cannot be of any direct 
benefit to his career path.<br>
<br>
<b>Gender blender</b><br>
A majority of jobs applied for by women are service oriented – customer 
relations, administrative positions in ministries and leisure and tourism 
related work. It would appear that women excel in these fields because they 
require special skills when dealing with clients and public alike. <br>
<br>
The industrial sector, however, displays a different approach. Male graduates 
enjoy a definite edge both during the selection process and subsequently in the 
performance of the job itself. The above holds true for the simple fact that:<br>
<br>
a) More men apply and pursue an education relevant to fields like chemical or 
mechanical engineering and also biochemistry. These fields have, in the past, 
been open only to the male gender and have been traditionally viewed as areas 
that only men should enter into. However, as times are changing, so are 
perceptions.<br>
<br>
b) The working hours in the industrial sector are generally not suited for women 
in Omani society, especially for single and unmarried women. Due to the 
two-shift policies usually applied in factories and in other industries, 
families prefer not to have their daughters, sisters or wives working late into 
the night.<br>
<br>
<img border="0" src="im94a.jpg" align="right" hspace="5" width="200" height="162">c) Most Omani IT and engineering graduates find jobs instantaneously after 
graduation. This is because these fields have displayed tremendous growth in 
Oman and also due to the government’s Omanisation drive. As education has become 
an essential and indivisible part of His Majesty’s vision for Oman, the fruits 
are now being reaped and the industrial sector is fast replacing expatriate 
workers with qualified local labour.<br>
<br>
d) Due to the Omani government’s encouragement of various industrial projects 
such as the Sohar Sea Port (which is set to become one of the biggest ports 
worldwide) and The Wave real estate project, there is a visible move by Omani 
students into the study of science subjects.<br>
<br>
<b>Business rules</b><br>
The faculty that is most sought after by students is the Business Faculty. But 
there is the flip side with there being more business graduates than jobs. This 
has resulted in many business graduates working in areas that don’t meet with 
their expectations. As most graduates have little work experience prior to their 
graduation, they tend to use their first job as a stepping-stone for better 
paying and more relevant jobs in the future.<br>
<br>
Often, most jobs are not advertised and fresh graduates hear about them through 
friends or family. In this sense, connections and networking play a major role 
in the job prospects. In many cases, this is a key element and, sometimes, the 
process of holding an interview to assess capabilities and ability is 
disregarded all together.<br>
<br>
Students who would take job placements during vacations are more likely to find 
a job and fill it straight away. During the placement periods, the students gain 
experience and, more importantly, make connections. These connections play their 
part during the future selection process.<br>
<br>
<b>Conclusion</b><br>
For a more productive workforce and lower unemployment figures, the State has to 
combine its efforts with the needs of the labour market to achieve higher 
synergy and increased productivity. The State must actively encourage 
high-school graduates to enter fields of study that are relevant to the job 
market in both the private and the public sector. While Omanisation opens up new 
jobs, a more strategically coordinated programme should be initiated with the 
aid of the private sector.<br>
<br>
This strategic programme should include training provided by the private sector, 
the cost of which should be shared by the government as well as the employer. 
This will result in a more effective employee who will be highly motivated. <br>
<br>
Private sector employers should be encouraged to allow their employees to pursue 
further education. I will even say that the fee should be partially borne by the 
employer. In return, the employee can put it in writing that he will meet the 
cost if he leaves the company before the completion of his tenure. <br>
