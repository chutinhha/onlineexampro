                    </font><b><font face="Verdana" size="4">Strategy Ideas In 
                    Foreign Exchange</font></b><font face="Verdana" size="1"><img border="0" src="images/img27.jpg" align="right" hspace="8" vspace="3" width="100" height="118"><br>
                    <b>The Euro is expected to fall in the next six months because 
                    the psychological zeitgeist of the global financial markets 
                    has shifted with the US economy emerging from recession far 
                    faster than the Eurozone or even Japan</b><br>
                    <br>
                    Is the epic fall in the dollar since March 2009 finally over? 
                    The dollar rallied after the June non-farm payroll report 
                    triggered a surge in the SP500 index, with the Euro/Dollar 
                    falling from 1.44 to below 1.42 in a single trading session. 
                    This aberrant behaviour violates the established correlation 
                    between the Euro and global equities as the Pavlovion reaction 
                    of currency traders was to sell dollars, buy Euros on any 
                    spike in risk appetite. After all, the March-August rally 
                    in the Euro from 1.24 to 1.44 coincided with the steepest 
                    rally in the American stock market since the Great Depression.<br>
                    <br>
                    <img border="0" src="images/img28.jpg" align="left" hspace="8" vspace="3" width="167" height="289"> 
                    Central banks from the Gulf, Southeast Asia and China also 
                    scrambled to buy Euros to reduce the dollar weighting in their 
                    reserves, estimated as more than two-thirds by the IMF. Yet 
                    I am now convinced that the Euro will fall, not rise in the 
                    next six months. Why? One, the psychological zeitgeist of 
                    the global financial markets has shifted. There is no longer 
                    a consensus to sell dollars on any indiscriminate risk rally 
                    but to buy dollars if US data improves. Two, central banks 
                    are reluctant to buy Euros above 1.40. Three, the US economy 
                    will emerge from recession far faster than the Eurozone or 
                    even Japan, whose business cycle lags behind America. Four, 
                    American Treasury bond yields are now far higher than German 
                    Bunds, unlike the case in March. It will not surprise me to 
                    see the dollar rise to 1.32-1.34 Euros by December.<br>
                    <br>
                    <b>Aussie-Kiwi prospects</b><br>
                    The Australian New Zealand cross rate (Aussie-Kiwi in the 
                    jargon of US currency gnomes!) has a credible prospect of 
                    making money in the next six months. It is never wise to ignore 
                    the public musings of central bank governors, let alone veiled 
                    threats of intervention. <img border="0" src="images/img30.jpg" align="right" hspace="8" vspace="3" width="166" height="342">New 
                    Zealand is an export dependent economy and its export include 
                    price elastic commodities like milk, meat and grains, not 
                    Australia’s iron ore, platinum, natural gas and gold. So it 
                    is not surprising that the Reserve Bank is horrified by the 
                    spectacular rise in the Kiwi dollar in the last six months. 
                    Governor Bollard has hinted that the central bank bias is 
                    for easy money, with credit tightening unthinkable until 2H2010. 
                    The Australian central bank has been far more hawkish in its 
                    belief that it need not wait until unemployment peaks. So 
                    while New Zealand policy rates (2.5 per cent now) could fall, 
                    Aussie rates (3 per cent) could rise. The Australian dollar 
                    will also benefit from the depreciation of the Japanese yen, 
                    as it is a favourite carry trade destination due to its interest 
                    rate advantage over the yen, dollar, Euro and Swiss franc.<br>
                    <br>
                    Naturally, this strategic trade idea in foreign exchange will 
                    lose money if China tightens aggressively and commodities 
                    prices collapse. This is a highly unlikely scenario, as Shanghai 
                    cannot slash bank credit growth since its export sector growth 
                    has collapsed. The Aussie-Kiwi dollar cross trading range 
                    could well be a buy at 1.24 and a sell at 1.32.<br>
                    <br>
                    <b>Yen condition</b><br>
                    The Japanese yen was exceptionally strong during the past 
                    two years as it was the world’s safe haven as Wall Street’s 
                    subprime mortgage crisis morphed into a global financial cataclysm. 
                    This was not surprising as the Swiss franc, the other traditional 
                    safe haven in times of financial panic, was tainted by the 
                    near collapse of UBS under Marcel Ospel and intervention by 
                    the Swiss central bank to weaken the Swissie and protect Switzerland’s 
                    industrial exports to the Eurozone. The Japanese yen soared 
                    from 118, when the credit crisis began to the 94-97 range 
                    now.<br>
                    <br>
                    However, Japan faces a financial Black Death next year even 
                    as the US economy emerges from recession. Japanese government 
                    debt to GDP is a shocking 200 per cent and an LPD loss in 
                    the general election could well hit Japan’s sovereign credit 
                    risk rating. Moreover, Japan’s trade surplus will disappear 
                    as international trade contracts. The Japanese public is also 
                    accelerating its purchase of higher yield bonds in markets 
                    as diverse as Turkey, Brazil, New Zealand and Australia.<br>
                    <br>
                    Meanwhile, the Bank of Japan must keep printing money and 
                    maintain near zero interest <img border="0" src="images/img29.jpg" align="left" hspace="8" vspace="3" width="362" height="221">rates 
                    as long as wages, retail sales and payrolls keep falling. 
                    Japan, unlike America, will not emerge from its worst recession 
                    since World War II. As volatility and systemic risk metrics 
                    on Wall Street decline, the financial markets will focus on 
                    Japanese economic fundamentals, not the yen’s safe haven role. 
                    This means the dollar-yen could well be headed to 105-110. 
                    <br>
                    <br>
                    <b>Cross-currents</b><br>
                    The Euro-Swiss franc cross rate is probably the most important 
                    monetary variable for the SNB, the Swiss central bank in Berne. 
                    At a time, when the Swiss private banking industry seems embattled 
                    by the financial meltdown at UBS and Swiss industrial exports 
                    to the Eurozone have plunged due to the global recession, 
                    the SNB does not want or need a rising franc. Unlike the ECB 
                    or the Fed, the SNB has intervened in the foreign exchange 
                    market to depreciate the Swiss franc against the Euro. If 
                    the SNB does not bring down the Euro-Swissie exchange rate, 
                    the Swiss economy will face deflation.<br>
                    <br>
                    The July US jobs number will lead to a greater appetite for 
                    financial assets, which erodes safe haven demand for the Swiss 
                    franc. This, in turn, means the SNB has given a gift to currency 
                    speculators, a gift with an asymmetric risk reward calculus. 
                    If the Euro-Swiss rises to 1.50-1.52, the SNB will intervene, 
                    as it did last March, to restrain the rising strength of the 
                    Swiss franc. Moreover, the US government’s crackdown on offshore 
                    tax evaders and Justice Department’s settlements with UBS 
                    are unquestionably long term bearish for the Swiss finance 
                    sector and the Swiss franc. The Euro-Swissie cross is a buy 
                    at 1.52 and a sell at 1.60.<br>
