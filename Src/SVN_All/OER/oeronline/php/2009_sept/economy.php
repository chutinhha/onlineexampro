		        <td valign="top" width="100%"> <p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
                    </font> <b><font face="Verdana" size="4">Lessons Learned</font></b><br>
                <b><font face="Verdana" size="1"><img border="0" src="images/img45.jpg" align="right" hspace="8" vspace="6" width="100" height="114"></font></b><font face="Verdana" size="1"><b>Almost 
                a year on from September 2008 and that fateful period which saw 
                global financial giants collapse an</b></font><font face="Verdana" size="1"><b>d 
                the world’s greatest economies teeter, the repercussions 
                continue to reverberate. Where has this left Oman though? Was 
                the confidence that Muscat’s business community displayed at the 
                end of 2008 unfounded?</b><br>
                <br>
                <br>
                At the end of last year, leaders in banking, government and 
                across other economic sectors were optimistic, though cautiously 
                so, that Oman could be spared from the wreckage suffered by the 
                US, Europe and others. Omani businesses felt confident because 
                of strong leadership from the Sultanate’s financial regulators, 
                an economy that stands slightly apart from those of Oman’s 
                neighbours, and the measured pace of growth the leadership has 
                nurtured.<br>
                <br>
                <b>Economic test</b><br>
                <img border="0" src="images/img46.jpg" align="left" hspace="8" vspace="5" width="166" height="387">By 
                summer 2009, consensus had developed that these predictions were 
                right. Oman has certainly suffered from the global financial 
                crisis, but the adverse effects were not of its own making, and 
                were, in the end, far smaller than they could have been. The 
                global financial crisis ultimately has been a good test for 
                Oman’s economic development thesis. It has helped sharpen 
                thinking in Muscat about how Oman fits into the region as a 
                whole, and its role and strengths in the wider global market. In 
                most areas, suspicions were confirmed and long-held values 
                reinforced.<br>
                <br>
                What Oman learned is that it is truly a unique country within 
                the GCC. Though it is tempting to look at the six members of the 
                group uniformly, as energy-rich countries challenged by the 
                prospect of economic diversification, the truth is more complex. 
                To be sure, oil still comprises the lion’s share of state 
                revenue across the GCC, and for as long as that remains the case 
                in Oman, its economy will be subject to the same external 
                factors as the rest of the GCC’s energy producers. Yet even 
                within the energy sector, Oman stands apart as the only major 
                GCC oil producer that has not joined OPEC, and is therefore not 
                subject to the cartel’s production quotas. Outside the energy 
                sector, however, is where the real differences are to be found 
                between Oman and its neighbours.<br>
                <br>
                Oman has a relatively large population, and that means 
                job-creation is a bigger factor here than in most GCC states. 
                Oman and Bahrain share the challenge of a post-oil future 
                looming nearer on the horizon than that of the other countries. 
                But Oman’s unique blend of these factors makes it special. Over 
                the past year, the mix proved useful to the Sultanate in keeping 
                its economy going. The demographics of the country also played a 
                huge role. Oman’s economy is 38 per cent driven by private 
                consumption, and the fact that 55 per cent of the population is 
                under 20 years of age will only increase this momentum. Consumer 
                power means Oman’s economy is less dependent on imports and less 
                likely to implode when oil prices plummet and foreign demand 
                drops. This gives the Sultanate an economic catalyst that some 
                of the sparsely-populated GCC states cannot enjoy, and that has 
                been evidenced by Oman’s relatively good performance.<br>
                <br>
                <b>Geographic edge</b><br>
                Though Oman is similar to its GCC brothers in some 
                characteristics, none can boast of the benefits of geography. 
                The Sultanate can count itself lucky in having a truly different 
                landscape: Oman not only has the legendary desert the Arabian 
                Peninsula is known for, but also boasts coastline, mountains, 
                and the greenery of Dhofar during the monsoon season. The 
                physical features of the land are important for two reasons. The 
                first is tourism.<img border="0" src="images/img47.jpg" align="right" hspace="8" vspace="7" width="165" height="393"> 
                While many Gulf states are trying to lure tourists, none but 
                Oman can boast of the natural beauty that many tourists are 
                looking for. This means tourism in Oman can eventually be more 
                diversified and resilient than anywhere else in the Gulf. During 
                the financial crisis Oman’s tourism strategy was not severely 
                affected, and does not need a massive rethinking.<br>
                <br>
                The second reason why geography is important is geostrategic. 
                Oman’s location at the southern tip of the Arabian Peninsula 
                means it has direct access to the Indian Ocean without requiring 
                passage through two chokepoints that have the potential to cause 
                major instability in maritime commerce. East of the peninsula, 
                in the Arabian Gulf, the Strait of Hormuz is a 54-kilometre-wide 
                passage between Iran and the Arabian Peninsula’s pointy tip, at 
                Musandam. An estimated 40 per cent of the world’s crude oil 
                shipped by sea must pass through this crucial waterway. Another 
                cyclone like Gonu could disrupt the ports, or render the 
                waterways temporarily perilous. <br>
                <br>
                And with the potential for both Iraq and Iran to destabilise the 
                neighbourhood, there is significant concern about keeping the 
                waterway peaceful. Kuwait, Bahrain, Saudi Arabia, Qatar and the 
                United Arab Emirates all rely on this waterway. West of the 
                peninsula, in the Red Sea, the Gulf of Aden and the Indian 
                Ocean, Somali pirates have jeopardised freighters in recent 
                years, and ships departing from Saudi Arabia’s western coast are 
                exposed to these maritime risks. Should the unthinkable happen 
                and both waterways were to become impassable, the entire Gulf 
                would likely be supplied through Oman. Oil and other exports 
                could be trucked to Omani ports, and all imports would make 
                their way throughout the GCC from Oman. <br>
                <br>
                It is precisely these geostrategic factors that give urgency to 
                the country’s strategy to develop its ports at Sohar, Duqm and 
                Salalah. These facilities have a competitive advantage over any 
                other GCC port because of their locations. The secondary effects 
                could be huge for Oman’s economy – if the ports do well, then 
                cities like Sohar could end up <br>
                as magnets for new industrial activity.<br>
                <br>
                <b>Regulatory strength</b><br>
                Apart from natural factors such as oil deposits, demographics 
                and geography, Oman can count itself as unique in the region for 
                the management of its financial-services economy, and can 
                attribute its relatively smooth ride through the crisis in part 
                to its financial regulations, which enjoy a reputation second to 
                none in the region. The Central Bank of Oman and the Capital 
                Markets Authority are considered paragons of prudence and 
                transparency. Oman’s banks suffered minimal exposure to the 
                US-originated toxic assets that sank banking giants worldwide, 
                and the central bank is uniformly credited for that 
                accomplishment.<br>
                <br>
                Another example of this prudent regulatory regime came in the 
                summer of 2009, when holding companies controlled by Saudi 
                Arabians announced that they would seek to restructure their 
                loans due to financial difficulties. Omani banks were the first 
                in the region to come clean about debts to the groups, which 
                triggered remarks across the GCC about such a level of 
                transparency. Bank Muscat’s Bahraini branch, as well as the 
                National Bank of Oman disclosed loan exposure to the group.<br>
                <br>
                With strong regulation, a geographic blessing and a unique mix 
                of domestic factors, which give it its edge in the GCC, Oman has 
                found itself in a good position a year on from the financial 
                dramas, which have become synonymous with the fourth quarter of 
                2008. While the crisis did not offer Oman much in the way of new 
                lessons, it did serve as a useful reminder of how Oman’s economy 
                works, why it has succeeded while other similar ones failed, and 
                how it can continue on the path to sustained prosperity as the 
                long anticipated green shoots of recovery emerge.</font><br>
