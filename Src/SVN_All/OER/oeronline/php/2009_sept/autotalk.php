                <p align="justify" style="margin-left: 10px; margin-right: 10px"><font face="Verdana" size="1">To 
                be viable as a sport-luxury exec, a vehicle has to tick the 
                right boxes. It has to look good, have a stylish interior, be 
                loaded with features, feel sporty and have a comfortable ride. 
                And to compete with its stiff competition it has to excel in 
                most, if not all, of these.<br>
                <br>
                Lets start with the most obvious one – the looks. With the CTS 
                it is a matter of love or hate, you either love it or you hate 
                it. Personally though, we absolutely love it. The CTS is a very 
                handsome car from practically any angle. It retains the bold 
                edges of the original, making the car distinctly Cadillac, 
                adding a raked back glass and an extra-wide C-pillar to make the 
                car look more like a coupe. Likewise, the heavily raked front 
                grille and the dual-purpose, centre brake light/spoiler 
                indicates its performance intent. Its decision to stick with its 
                predecessors design trend and offer up an angular look portrays 
                modernity as well as class. Thankfully Cadillac hasn’t gone 
                overboard with the design and as a result the car will look 
                perfectly at home alongside its German and Japanese 
                counterparts. <br>
                <br>
                The same follows suit on the inside as well. It is angular, 
                stylish, classy and sporty all at the same time. Even more 
                surprising is the fact that it has struck an almost perfect 
                balance between each of the four factors. Once inside, you are 
                surrounded by high quality leather and burled walnut trim. With 
                the correct choice of interior colour and the available ambient 
                lighting in the car, the cabin can be a very accommodating place 
                for long drives. The only nagging criticism that we have of the 
                interior is the centre console which, although very stylish and 
                good looking, is a tad bit too cluttered for our tastes and the 
                material used is a minor let-down in comparison to the rest of 
                the cabin’s high quality standards. The CTS more than makes up 
                for this by offering oodles of kit to play around with. You have 
                everything you could possibly want in a car – A high quality 
                surround sound system with a built in hard-drive, full power 
                adjustments for the driver and front passenger, ventilated 
                seats, keyless entry, sat-nav with a nifty pop-up screen and a 
                lot more. Overall, the cabin is very nice place to be and, in 
                our opinion, is by far the best looking interior among the 
                Cadillac range.<br>
                <br>
                <img src="images/img89.jpg" width="350" height="179" hspace="8" vspace="5" border="0" align="left">American cars are not really known for their handling 
                characteristics but this is where the CTS differs. The car, 
                namely the CTS-V, was exhaustively tested and tuned at Germany’s 
                legendary Nurburgring circuit. For those of you who are not 
                familiar with the track – ‘The Ring’ is a 13 mile circuit with 
                over 180 corners and is considered to be the toughest, most 
                dangerous and most demanding purpose-built race track in the 
                world. It is not affectionately called ‘The Green Hell’ for 
                nothing! The result of these tests shines through once you are 
                behind the wheel. The car is just as good on twisting mountain 
                roads as it is on the highway and with a 3.7l V6, producing 
                326hp, under the bonnet it is no slouch either. Unfortunately, 
                its girth prevent it from being as agile as it could be. This 
                also means that you will have to rev the engine hard to truly 
                experience the true capability of its great chassis. But, as if 
                to make up for this, the CTS does entice you to push it harder 
                with a very delightful engine soundtrack that any motor-head 
                will enjoy. We can just imagine what the CTS-V must be capable 
                off, especially with its sport suspension setup and 556 insane 
                rampaging horses under the bonnet.<br>
                <br>
                Overall, it seems that the CTS ticks all the right boxes. It is 
                good-looking, modern, sporty and feature rich. It is also safe 
                and aside from some slight wind and road noise, which is really 
                not as obtrusive as you might think, it is quite refined as 
                well. All-in-all it makes for a great alternative that should 
                not be overlooked if you are in the market for such a car, 
                especially so if you like its looks.<br>
