                <td height="18"> <b></b> <p align="justify" style="margin-left: 10px; margin-right: 10px"><b><font face="Verdana" size="4"><br>
                    American Born German Trained</font></b><font face="Verdana" size="1"><br>
                    <br>
                    <b>If you are looking for a sporty, mid-sized luxury saloon, 
                there are quite a few models to choose from. The </b><b>new 
                Cadillac CTS is one of them. but is it a head-turner? Malcolm 
                Xavier Crasta took one out for a spin around Muscat to answer 
                just this question</b></font></p>
				  <table border="0" cellpadding="0" cellspacing="0" style="border-collapse: collapse" bordercolor="#111111" width="100%">
                    <tr>
                      <td width="100%">
                      <p align="center"><font face="Verdana" size="1">
