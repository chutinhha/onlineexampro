                  <li dir="ltr"> 
                  <p align="justify" style="margin-top:0; margin-bottom:0" dir="ltr"><font face="Verdana" size="1">
                  Projects using new or advanced technology</font></li>
                  <li dir="ltr"> 
                  <p align="justify" style="margin-top:0; margin-bottom:0" dir="ltr"><font face="Verdana" size="1">
                  Projects that meet international market demand
                    </font>
                  </li>
                  <li dir="ltr"> 
                  <p align="justify" style="margin-top:0; margin-bottom:0" dir="ltr"><font face="Verdana" size="1">
                  Projects that involve the integrated use of China’s resources 
