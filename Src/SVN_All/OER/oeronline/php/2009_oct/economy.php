                    Forum’s (WEF’s) Global Competitiveness Index 2009-10 and the 
                    World Bank’s Doing Business Report 2010. Both offered a glimpse 
                    of the progress toward greater international integration that 
                    Oman has made in recent years.<br>
                    &nbsp;<br>
                    As much as the reports give us a yardstick of success, they 
                    can also be seen as a measure of what else can be done. Oman 
                    is now ranked as the 41st (out of 133) most competitive economy 
                    in the WEF Report, and took the number 65 spot (of 131) on 
                    the Doing Business report, making it the seventh highest ranked 
                    country in the MENA region.The competitiveness index, for 
                    example, is made up of three ‘pillars’: Basic Requirements, 
                    Efficiency Enhancers and Innovation and Sophistication Factors. 
                    Oman performed best in the first pillar – which covers infrastructural 
                    and institutional health – ranking number 25, up from number 
                    31 last year. It scored especially well in the macroeconomic 
                    stability benchmark, at number 10 in the world. <br>
                    <br>
                    <b>Notable gains</b><br>
                    Indeed, Oman has been relatively well-protected from the global 
                    crisis. Along with Bahrain and Qatar, it is one of three countries 
                    in the GCC for which th<img border="0" src="images/img33.jpg" align="left" hspace="8" vspace="5">e 
                    IMF is predicting positive growth rates for 2009. The economy 
                    is expected to expand by 3 per cent this year, outpacing the 
                    predicted average of 2.5 per cent for the Middle East and 
                    well above the forecast contraction of 1.3 per cent worldwide.<br>
                    <br>
                    Another area of improvement on the WEF competitiveness index 
                    is ‘technological readiness’, a category that was aided by 
                    recent network upgrades and the state’s continual roll-out 
                    of more expansive e-government services. Oman’s score on this 
                    ranking increased by nearly half a point (a significant rise 
                    on a scale of seven) compared to last year’s report.<br>
                    <br>
                    While the Sultanate has made notable gains in some areas, 
                    progress has been slower in others. It fell by four places 
                    to number 52 on the ‘Innovation and sophistication factors’ 
                    pillar of the WEF report. However, this may have more to do 
                    with other countries going up in the rankings.</font><font face="Verdana" size="4"><b><br>
