                    </b></font><font face="Verdana" size="1">The ‘Innovation’ 
                    pillar, which essentially measures local entrepreneurship 
                    activity, proved to be a weak spot throughout the region. 
                    In all the countries of the GCC their ranking on the ‘Innovation’ 
                    pillar was consistently lower than their respective overall 
                    ranking in the WEF report. The results of Oman and its GCC 
                    counterparts in the studies reveal the interesting paradox 
                    of business in the region: while foreign investors and developers 
                    have enjoyed an increasingly warm welcome over the past few 
                    years, organic growth in the business world has yet to post 
                    similar rates of expansion. Both the infrastructural and cultural 
                    framework to encourage local entrepreneurship has only very 
                    recently begun to take hold. <br>
                    <br>
                    <b>SMEs role in development</b><br>
                    The cultivation of small and medium sized enterprises (SMEs) 
                    and an entrepreneurial spirit is essential for pushing development 
                    to the next stage – a thought that has surely not been lost 
                    on minister of commerce and industry HE Maqbool bin Ali Sultan 
                    and other government leaders. The state has been making a 
                    concerted effort over recent years to encourage small businesses 
                    through financing options and mentorship programmes. Encouragement 
                    is coming from both governmental and non-governmental agencies, 
                    with Intilaaqah leading in the private sector and the Directorate-General 
                    of SME Development, under the ministry of commerce and industry, 
                    heading the public sector, among several other state-run programmes. 
                    <br>
                    <br>
                    While financing will likely be an issue throughout 2009 in 
                    Oman and the rest of the world, the Sultanate at least has 
                    a solid lending infrastructure. In 2006 the lending limit 
                    of the Oman Development Bank (ODB) – which has provided soft 
                    loans to aid development projects since 1977 – was raised 
                    by Royal Decree from RO165,000 to RO1mn. The same decree doubled 
                    the bank’s capital from RO20mn to RO40mn. The government-owned 
                    bank issued RO35.9mn worth of loans in 2008, up 20 per cent 
                    from 2007 lending levels of RO29.9mn. <br>
                    <br>
                    The SMEs were given a boost this year by the OCCI’s establishment 
                    of a Business Diagnostic Centre – which is envisaged as a 
                    business ‘clinic’ that offers assistance to fledgling and 
                    existing start-ups. The World Bank’s ‘Doing Business’ report, 
                    which ranked Oman at number 65 of 131, also noted streamlining 
                    reforms in the start-up process. In line with the state’s 
                    e-government strategy, an online name registry for businesses 
                    was recently unveiled, which allows payment of start-up costs 
                    with a prepaid card, according to the report.<br>
                    <br>
                    <b>Business reforms</b><br>
                    All these developments are promising and certainly illustrate 
                    Oman’s commitment to business reform. But there is also recognition 
                    of what more needs to be done to increase competitiveness. 
                    According to comments from the chairman of the Oman Chamber 
                    of Commerce and Industry (OCCI) at last February’s SME Exhibition 
                    in Muscat, around 60 per cent of businesses operating in the 
                    Sultanate can be classified as SMEs, compared to 90 per cent 
                    in most developed countries.<br>
                    <br>
                    As the economy continues to diversify from hydrocarbons, small 
                    private firms will feature as important sources of economic 
                    growth and – crucially – job creation, as the government seeks 
                    to employ more nationals.<br>
                    <br>
                    Central Bank data for 2007 shows that the private sector accounted 
                    for 85 per cent of all jobs, with just under half of these 
                    jobs occupied by Omani nationals. Indeed the private sector, 
                    much of which is held by SMEs, is the engine of job creation, 
                    with the number of persons employed in the sector increasing 
                    by 20-23 per cent from 2006-2008, compared to 5-6 per cent 
                    (2006-07) growth in the public sector.<br>
                    <br>
                    <b>Local employment</b><br>
                    But the growing number of jobs in the private sector has not 
                    been met with an equal rise in Omani employment. The sector 
                    remains dominated by expatriates, and nationals are outnumbered 
                    by a ratio of about 5:1. This is another common theme in the 
                    Gulf, as rapid economic growth has both attracted, and been 
                    aided by, an influx of foreign workers over the past few years.<br>
                    <br>
                    The Omanisation programme should eventually redress this imbalance 
                    and in July the ministry of manpower increased the minimum 
