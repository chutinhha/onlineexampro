                    agenda for the coming years. But in many ways, the onus of 
                    business and job creation will fall on the youth of Oman. 
                    According to figures from the ministry of national economy, 
                    over 80 per cent of the Omani national population is under 
                    the age of 30. As the country continues to move toward a more 
                    diversified, knowledge-based economy, an ethos of innovation 
                    and self-starters will be crucial in kicking the Sultanate’s 
                    economy into the next gear.<br>
                    <br>
                    In the competitiveness report, the WEF characterises Oman 
                    as a state in transition, moving from an ‘efficiency-driven’ 
                    to an ‘innovation-driven’ economy. The Sultanate and other 
                    Gulf states have already proven – and continue to prove – 
