                </b><img src="images/img79.jpg" width="375" height="396" hspace="9" vspace="6" align="right">It’s 
                a very popular saying that you need to spread your eggs in several 
                baskets and for me that’s been working perfectly fine,” says Indranil 
                Dutta, group general manager, Double Crown Group. One look at 
                his background and it is clear that variety is not something that 
                he lacks. A mechanical engineer from BIT Mesra in Ranchi, India, 
                his first job was with a company called Tega India. This company, 
                coincidentally, was a case study for him at XLRI, Jamshedpur – 
                where he pursued a management course soon after leaving Tega. 
                After passing out from XLRI, he joined a group called Balmer Lawrie. 
                <br>
                <b><br>
                Down memory lane<br>
                <br>
                </b>“It was a diversified group and I was instrumental in setting 
                up a subsidiary called the ‘Indian Container Leasing Company’ 
                says Dutta, “At that time in India, it was quite a revolutionary 
                concept.” He also helped in establishing the brand name of the 
                organisation – Transafe. <br>
                <br>
                Today Transafe is doing quite well, with offices all over the 
                country. His objective was to start from the grass roots and take 
                it to a respectable level. “I did my part and then decided to 
                move on to newer challenges. So I left the company” says Dutta. 
                By that time the brand was already well established and things 
                were pretty much on their way. So, after five years with the Indian 
                Container Leasing Company he joined Exide Industries as a branch 
                manager and was with them for seven years. After developing the 
                company’s branch at Jamshedpur, he moved on as the regional manager 
                in Kolkata, heading the Eastern region. Later he was given charge 
                of the vibrant Western India operations as regional manager, operating 
                out of Mumbai.<br>
                <br>
                After that, he headed to Oman and joined the auto industry for 
                the first time. He was with the OTE Group from 2002 till December 
                2008, as the senior manager in charge of the Subaru Division and 
                later Isuzu and Ssangyong. He then joined the Double Crown Group 
                and has been there since. “Joining this group was a paradigm shift 
                and I did so consciously. Because, today, you need to be a generalist 
                to keep the larger picture in focus”, quipped Dutta, “This was 
                a very challenging business for me.”<br>
                <br>
                He started his work at the Double Crown Group sometime in December 
                last year. Says Dutta, “My aim was to maintain a balance between 
                adding value to the existing divisions and simultaneously working 
                on new business areas.” Since the company itself was fairly new, 
                a lot of systems needed to be put in place. So, his initial task 
                was to establish these systems and processes on a firm footing. 
                “This took up two to three months time. Once done, the second 
                priority was to go hammer and tongs for both topline and bottomline. 
                This involved both renovation as well as innovation and now almost 
                everything is on track” added Dutta.<br>
                <br>
                    Dutta has plans to foray into a number of businesses like 
                    real-estate, logistics, hospitality and probably even wind 
                    power generation in future. How these ventures will go is 
                    yet to be seen but judging from his past ventures and successes, 
                    these should not pose a major problem.<br>
