		        <td valign="top" width="100%"> <p align="justify" style="margin: 3px 10px" dir="ltr"><font face="Verdana" size="1"> 
                    <br>
                    </font><b><font face="Verdana" size="4">Effortless Interaction</font></b><font face="Verdana" size="1"><br>
                    <b>The need to streamline the interaction between users and 
                    machines is now more important than ever. Malcolm Xavier Crasta 
                    tracks the ongoing developments in the field to see what is 
                    in store for us</b><br>
                    <br>
                    <img border="0" src="images/img36.jpg" align="right" hspace="8" vspace="5"> 
                    Currently there are over a billion personal computers in use 
                    around the world. Over 1.5mn game consoles were sold worldwide 
                    in the month of August, 2009. The number of mobile phones 
                    in use is sure to far exceed these. All things considered 
                    it is quite clear that the two primary uses we have of electronic 
                    devices are communication and entertainment. Today it is hard 
                    to imagine our lives without them. And with so many devices 
                    in use worldwide it is imperative that they are made user-friendly. 
                    Luckily for us, stiff competition and mammoth advancements 
                    in technology have since streamlined the way in which we interact 
                    with these machines. Luckily, they are also getting better, 
                    smarter and faster with every new generation.<br>
                    <br>
                    <b>The first human interface</b><br>
                    Initially<img border="0" src="images/img37.jpg" align="left" hspace="8" vspace="5">, 
                    all we really had at our disposal, while dealing with computers, 
                    was a basic keyboard, mouse and maybe a joypad. Although these 
                    devices served their purpose well, they were not convenient, 
                    needing drivers to be installed prior to use. This made adding 
                    different gadgets a cumbersome affair. So, it was only logical 
                    that the first thing that needed to be done was to streamline 
                    connectivity. While there were quite a few minor advancements 
                    over the years, it was only with the introduction of USB interface 
                    in 1996 that plug-and-play devices truly took over. The was 
                    intended to replace the multitude of connectors at the back 
                    of PCs, as well as to simplify software configuration of communication 
                    devices. While it may not have reduced the number of connectors, 
                    it definitely reduced their sizes and made them significantly 
                    easier to use. Another side-effect of the USB interface was 
                    the freedom it gave manufacturers to build much more advanced 
                    yet easier to use devices. It is a similar story with nearly 
                    every other device. As an example – telephones advanced further 
                    thanks to better display technology, Bluetooth, infrared and 
                    other such improvements in addition to incorporating USB connectivity.<br>
                    <br>
                    <b>Touch technology</b><br>
                    One of the biggest advancements in human-computer interactivity 
                    in recent years was the touchscreen. They first came into 
                    use somewhere in the late 1960’s but it wasn’t until 1972 
                    that they gained popularity, thanks to the computer-assisted 
                    learning term<img src="images/img38_new.jpg" width="149" height="158" hspace="8" vspace="5" border="0" align="right">inal 
                    that was part of the PLATO project. Today though, you see 
                    them practically everywhere from ATM’s to kiosks and even 
                    on quite a few phones. Its primary purpose initially was to 
                    alleviate the need for using a mouse or a direction pad wherever 
                    possible and, with recent advancements, to do away with the 
                    need of intermediate devices such as a stylus. <br>
                    <br>
                    Since its widespread use in computers and mobile phones, touch 
                    technology has seen a number of advancements primarily to 
                    increase it’s fluency, quality and response. But one of the 
                    most notable advancements has been multi-touch technology. 
                    Infact multi-touch technology was first invented by Nimish 
                    Mehta at the University of Toronto in 1982. But it wasn’t 
                    until Apple launched the iPhone and Microsoft unveiled surface 
                    computing that multi-touch truly picked up steam. In a few 
                    years multi-touch will be common place in our lives. The primary 
                    reason for this being that the new Microsoft Windows 7 operating 
                    system will ship with multi-touch support. In fact a number 
                    of laptop manufactures have already made plans to launch their 
                    new systems with the technology once the new operating system 
                    goes public.<br>
                    <br>
                    <b>Future of interactivity</b><br>
                    All these advancements are fine but where does the true future 
                    of interactivity lie? To answer this question, one only needs 
                    to look at the largest sector of the entertainment industry 
                    – video games. <br>
                    <br>
                    To find out what the future holds we have to first dig deeper 
                    into past and current technologies being used in games and 
                    focus on a few key technological developments. The first of 
                    w<img border="0" src="images/img39.jpg" align="left" hspace="8" vspace="5">hich 
                    is the PlayStation 2’s Eye Toy. This device was the first 
                    in line on the way to eliminating controllers from our hands. 
                    It was conceived by Richard Marks in 1999. His idea was to 
                    use the power of the PlayStation 2 (PS2) and with the help 
                    of a webcam to incorporate these two technologies – computer 
                    vision and gesture recognition, to enable a person to interact 
                    with a game using just gestures. The Eye toy was succeeded 
                    by the PlayStation eye for the PS3 in 2007. The new peripheral 
                    is claimed to have four times the resolution, two times the 
                    frame-rate of the EyeToy and twice the sensitivity. It also 
                    features a built-in four-capsule microphone-array for directional 
                    voice location tracking, echo cancellation and background 
                    noise suppression allowing for much more accurate gaming.<br>
                    <br>
                    The second key advancement was the development of the accelerometer, 
                    currently used for a variety of applications such as auto 
                    screen rotation in mobile phones when tilted and in some cases 
                    even for gesture commands wherever supported. Accelerometers 
                    are used in a number of industries ranging from automobiles 
                    to medical applications but nowhere is it more prominent to 
                    the public that with the Nintendo Wii.<br>
                    <br>
                    The Wii remote, or ‘Wiimote’ as most people call it, was revealed 
                    at the Tokyo Game Show in 2005. Using the remote, users can 
                    control games using gesture commands and by pointing towards 
                    the screen, thanks to the use of both accelerometer and optical 
                    sensor technology. It is this control scheme that has helped 
                    the Wii maintain its lead in console sales, despite it being 
                    the least powerful of the current generation of systems. And 
                    as if this wasn’t enough, Nintendo has just upped the ante 
                    by releasing the Wii MotionPlus which is an attachment that 
                    uses an angular rate sensor to supplement the Wiimote. The 
                    end result – a near perfect real-time motion capture of your 
                    gestures. Not to be outdone Sony is set to release its own 
                    take at motion controls sometime next year for the PS3. This 
                    controller, in addition to accelerometers and an angular rate 
                    sensor will have two colour-changing orbs that can be tracked 
                    by the aforementioned PlayStation Eye. These orbs allow the 
                    controllers position to be tracked in three dimensions, simply 
                    by calculating the controllers distance from the PlayStation 
                    Eye. This enables extremely high precision and accuracy with 
                    practically no lag whatsoever, as opposed to other camera-based 
                    control techniques on the PS3.<br>
                    <br>
                    <b>Saving the best for last</b><br>
                    While both, Nintendo and Sony, have very promising technologies 
                    to look forward to, in the end it is Microsoft that steals 
                    the show. Microsoft unveiled ‘Project Natal’ for the Xbox 
                    360 at this year’s E3, just prior to, Sony’s motion controller. 
                    ‘Project Natal’ is a code name for a revolutionary new way 
                    to play, without any controllers. See a ball? Kick it, hit 
                    it, trap it or catch it.<br>
                    <br>
                    Unlike 2-D cameras and controllers, Natal tracks your full 
                    body movement in 3-D, while responding to commands, directions, 
                    even a difference of emotion <img border="0" src="images/img40.jpg" align="left" hspace="8" vspace="5">in 
                    your voice. The primary devise is in actuality a combination 
                    of a number of devices that work in tandem to give you the 
                    full ‘Natal’ experience. These include a RGB camera, depth 
                    sensor, multiarray microphone and a custom processor running 
                    proprietary software. The RGB camera component has a video 
                    camera that delivers the three basic colour components and 
                    helps enable facial recognition and more. An infrared projector 
                    combined with a monochrome CMOS sensor makes up the depth 
                    sensor. This enables the device, and in turn the Xbox 360, 
                    to see the room in 3-D (as opposed to inferring the room from 
                    a 2-D image) regardless of the lighting conditions. The multiarray 
                    microphone works in pretty much the same was as the one found 
                    on the Eye, in that it will be able to locate voices by sound 
                    and extract ambient noise. But the most important part of 
                    the system is the custom processor, running Microsoft proprietary 
                    software layer. It is this proprietary software layer that 
                    makes the magic of “Project Natal” possible. This layer differentiates 
                    Natal from any other technology on the market through its 
                    ability to enable human body recognition and full-body tracking 
                    with the unmost accuracy. This technology in conjunction with 
                    the voice recognition software can also be used for a number 
                    of day-to-day applications such as controlling DVD’s, chatting, 
                    online shopping at the marketplace and more without the need 
                    for any other peripheral such as a controller or remote.<br>
                    <br>
                    When Natal is ready, sometime next year, it will work with 
                    every Xbox 360 console so people who have never picked up 
                    a controller can finally join the fun. Welcome to the future 
                    of human-computer interactivity.</font>
