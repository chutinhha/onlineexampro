                    </font><b><font face="Verdana" size="4">Philippines - A Business 
                    Friendly Archipelago</font></b><font face="Verdana" size="1"><br>
                    <b>As an emerging economy, the Philippines provides many opportunities 
                    to i</b><b>nvestors. Its strategic geographic location m</b><b>akes 
