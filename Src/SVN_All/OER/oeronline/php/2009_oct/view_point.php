                    <br>
                    </b><img border="0" src="images/img42.jpg" align="right" hspace="8" vspace="5" width="100" height="118">The 
                    Republic of the Philippines is an archipelago in the Western 
                    Pacific Ocean, consisting of more than 7,100 islands with 
                    a free market economy. Both foreigners and citizens enjoy 
                    economic freedom to engage in trade and business, subject 
                    to certain regulations. Major businesses are concentrated 
                    in urban areas, especially the Manila Metropolitan Area where 
                    employment opportunities are higher. The major industries 
                    in the country are agriculture, mining, manufacture of electronic 
                    components, and the booming industry of services such as call 
                    centers and business process outsourcing. The Philippines 
                    is rich in mineral and thermal resources, with considerable 
                    hydroelectric generation facilities. <br>
                    <br>
                    The Philippine business environment is highly personalised. 
                    Filipinos are friendly, warm, flexible and adaptable. They 
                    are hard working and very skilled. They are highly educated 
                    and the managerial talent is deep and wide. Although Filipino 
                    is its national language, the country’s official language 
                    for education and communication is English, making Philippines 
                    the third largest English speaking country. These qualities 
                    have given Philippines an edge in attracting business investors. 
                    <br>
                    <br>
                    The Philippine government offers incentives to firms, both 
                    domestic and foreign, to attract them to invest in priority 
                    areas of the economy. Domestically incorporated enterprises 
                    such as those registered with the Board of Investments and 
                    Philippine Economic Zone Authority may enjoy fiscal and non-fiscal 
                    benefits. To qualify for incentives, an enterprise must engage 
                    in an area of activity listed in the Investment Priorities 
                    Plan (IPP), which the government updates and issues on an 
                    annual basis. If not listed in the IPP, a Filipino-owned enterprise 
                    will be entitled to incentives if at least 50 per cent of 
                    its production is for export. For enterprises with more than 
                    40 per cent foreign equity, incentives can be availed of if 
                    70 per cent of its production is for export.<br>
                    <br>
                    <b>Investing in the Philippines</b><br>
                    Investments are welcome in the Philippines. Generally, under 
                    the Foreign Investment Act of 1991, the domestic Philippine 
                    market is open to foreign investors as long as the activity 
                    they seek to engage in is not in the Foreign Investment Negative 
                    List. An investor who wants to do business in the Philippines 
                    has to register with a number of government offices. These 
                    include the Securities and Exchange Commission (SEC), the 
                    Department of Trade and Industry (DTI), the Bureau of Internal 
                    Revenue (BIR) and the local government. The SEC is the government 
                    agency responsible for registering, licensing, regulating 
                    and supervising all corporations and partnerships organised 
                    in the Philippines, including foreign corporations licensed 
                    to engage in business or to establish branch offices in the 
                    Philippines. <br>
                    <br>
                    <b>Structure of business entities</b><br>
                    In the Phili<img border="0" src="images/img43.jpg" align="left" hspace="8" vspace="5">ppines, 
                    various organisational structures may be adopted. These structures 
                    are sole proprietorship, partnership, corporation, branch 
                    office, representative office, regional headquarters and regional 
                    operating headquarters.<br>
                    <br>
                    Sole proprietorships and partnerships are generally subject 
                    to lesser statutory requirements than companies, but the partners 
                    and the proprietors do not have the protection of limited 
                    liability. Unlike a sole proprietorship, a partnership is 
                    considered and treated as a juridical entity separate and 
                    distinct from its members or partners.<br>
                    <br>
                    A corporation is an artificial being created under the Corporation 
                    Code, with a separate and distinct identity. Stock corporations 
                    are not required to have any minimum authorised capital stock. 
                    However, the paid-up capital must at least be P5,000.00. Corporations 
                    organised under the Corporation Code are regulated by the 
                    Securities and Exchange Commission (SEC).<br>
                    <br>
                    A corporation can either be a stock or non-stock company regardless 
                    of nationality. A company is considered a Filipino corporation 
                    if 60 per cent stock is Filipino-owned and 40 per cent is 
                    foreign-owned. If more than 40 per cent stock is foreign-owned, 
                    it is considered a domestic foreign-owned corporation. A branch 
                    office is a foreign corporation organised and existing under 
                    foreign laws that carries out business activities of the head 
                    office and derives income from the host country. The minimum 
                    capital requirement for a branch office is $200,000 which 
                    can be reduced to $100,000 if the activity involves advanced 
                    technology, or if the company employs at least 50 direct employees. 
                    Registration with the SEC is mandatory.<br>
                    <br>
                    A representative office is a foreign corporation organised 
                    under foreign laws. It cannot engage in any income generating 
                    business in the Philippines. It only acts as a liaison office 
                    and deals directly with the clients of the parent company. 
                    A representative office undertakes activities such as information 
                    dissemination, promotion of products and services and quality 
                    control. The minimum capital requirement for a representative 
                    office is $30,000. Registration with the SEC is mandatory.<br>
                    <br>
                    <b>Philippine taxes</b><br>
                    The taxes imposed on businesses by the Philippine government 
                    are income tax, VAT (value-added tax), estate and donor’s 
                    tax, excise tax, documentary stamp tax and percentage tax. 
                    In addition the local government imposes local business taxes 
                    and real property taxes. The regular corporate income tax 
                    is 30 per cent of the net taxable income. Qualified businesses, 
                    which are granted special treatment under special laws, enjoy 
                    other tax regimes such as income tax holiday and the five 
                    per cent preferential tax on gross income in lieu of all national 
                    and local taxes. <br>
                    <br>
                    Income derived by non-resident individuals and corporations 
                    are generally subject to final withholding tax of 25 per cent 
                    and 30 per cent, respectively, subject to the provisions of 
                    the applicable tax treaty if the income recipient is a resident 
                    of a treaty country with which the Philippines has a tax treaty. 
                    Currently, the Philippines is a signatory to 37 tax treaties. 
                    Treaty with GCC countries are currently under negotiations.<br>
                    <br>
                    <b>Employment </b><br>
                    The business process outsourcing (BPO) has been in rapid growth 
                    for the past decade in terms of employment of Filipino nationals. 
                    Many businesses, a large number of which are call centers, 
                    transfer their satellite offices to the Philippines because 
                    of the low-cost of outsourcing in the country. Realising the 
                    potential of the fast growing outsourcing and services industry, 
                    the Philippine government has created more incentives for 
                    businesses investing in the Philippines, particularly those 
                    located in special economic zones. With the Filipinos’ skill 
                    in the field of information technology and their English language 
                    proficiency, the Philippines has become an ideal location 
                    for business investors.<br>
                    <br>
                    Unless otherwise exempt by specific provision of law, all 
                    imported goods destined for Philippines for consumption are 
                    subject to the payment of import duty prior to release. In 
                    addition to this and even in cases where the articles or commodities 
                    are imported duty-free, VAT is imposed on all imported articles 
                    at the rate of 12 per cent of the total landed cost. In some 
                    case, imported articles may be subject to ad valorem tax. 
                    Among the country’s major imports are petroleum, transportation 
                    equipment, metals and chemicals, and foodstuffs while the 
                    main exports are electrical and electronic components, textiles, 
                    coconut products, and fish.<br>
                    <br>
                    With the distinct combination of quality human resource, end-to-end 
                    digital connectivity and the government’s strong support for 
                    the IT industry, the Philippines offers a cost-effective location 
                    for back-office functions. The country is also positioned 
                    as a regional centre for ICT. All this makes investing in 
                    the Philippines very promising and attractive to investors.<br>
