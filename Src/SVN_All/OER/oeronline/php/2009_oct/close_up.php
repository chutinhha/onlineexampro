                <td height="18"> <b></b> <p align="justify" style="margin-left: 10px; margin-right: 10px">
				<b><font face="Verdana" size="4"><br>
                    Labour market - challenge 
                    in Bahrain</font></b><font face="Verdana" size="1"><br>
                    <img border="0" src="images/img67.jpg" align="right" hspace="6" vspace="5" width="100" height="121"><b>Bahrain’s 
                    labour market faces key shortcomings notably overrepresentation 
                    of foreign workers besides the phenomenon of disguised unemployment 
                    and underemployment. Certainly this is not sustainable</b><br>
                    <br>
                    Recently published statistics point out to declining representation 
                    of Bahraini nationals in the workforce, reaching less than 
                    a quarter of the total according to the latest statistics. 
                    According to figures released by the Labour Market Regulatory 
                    Authority (LMRA), locals comprised exactly 23.1 per cent of 
                    total employment in the second quarter of 2009. Bahrainis 
                    constituted 138,665 of 599,017 of total workers, with non-nationals 
                    comprising the balance workers. By comparison, the level of 
                    Bahrainisation stood at a higher 23.9 per cent in the first 
                    quarter of 2009 but still lower than 24.2 per cent in the 
                    last quarter of 2008.<br>
                    <br>
                    LMRA officials attributed the matter to stronger employment 
                    of foreign workers in construction and trade sectors. However, 
                    there is little sense in this argument amidst the on-going 
                    global financial crisis with all its adverse consequences 
                    on economic activities. Ostensibly, LMRA issued this general 
                    statement for lack of a comprehensive answer. One thing for 
                    sure, Bahrain’s economy is creating job opportunities but 
                    not necessarily those popular with locals. <br>
                    <br>
                    <b>Jobless debacle</b><br>
                    Officials at Bahrain’s labour ministry put the number of unemployed 
                    at 5,616 in August, suggesting a jobless rate of just above 
                    four per cent. The jobles<img border="0" src="images/img66.jpg" align="left" hspace="6" vspace="5" width="150" height="236">s 
                    rate is calculated by dividing the number of unemployed on 
                    locals in the job market rather than the entire workforce. 
                    Certainly, this is a fair assessment, as foreign workers should 
                    be in the country on contractual agreements for specific jobs. 
                    Still, it is believed that the real number of unemployed is 
                    higher, as some opt not to register with the labour ministry, 
                    as they have given up hopes of finding suitable employment 
                    chances. This is particularly true with females.<br>
                    <br>
                    More importantly, the jobless rate underestimates the full 
                    picture of the challenge, as the labour market suffers from 
                    cases of disguised unemployment and underemployment. Disguised 
                    unemployment refers to the phenomenon of hoarding workers 
                    for they have little or no use while sacking them is politically 
                    risky. For example, some 1,000 people work for Bahrain Radio 
                    and Television Corporation, undoubtedly overstaffed in the 
                    age of technology. However, this is one method used by officials 
                    to limit the unemployment problem. Nevertheless, Bahrain pays 
                    a price for such a tactic including allocating a sizable amount 
                    of budget on current expenditures. For fiscal year 2009, total 
                    spending is divided between 85 and 15 per cent on current 
                    and non-current spending, respectively. Undoubtedly, capital 
                    spending helps address issues related to infrastructure development, 
                    while non-current spending merely maintains the status quo.<br>
                    <br>
                    Yet, the jobless rate overlooks the challenge of underemployment 
                    or situations when workers accept employment but not in the 
                    desired concerns whether in terms of pay, hours or level of 
                    skill and experience. There are nationals who accept employment 
                    opportunities not commensurate with their educational and 
                    working experiences if only to secure a source of income. 
                    I personally have come across a Bahraini national with a college 
                    degree working as a bag carrier in a local supermarket. A 
                    good number of Bahrainis find themselves in the difficult 
                    situation of either accepting jobs below their qualifications 
                    or risk being jobless.<br>
                    <br>
                    <b>No sponsorship</b><br>
                    Prospects of Bahrain’s labour market and employment of locals 
                    are open to all possibilities following implementation of 
                    a law from beginning of August that grants expatriates workers 
                    the right to switch jobs. Article number 25 of the revised 
                    labour law grants foreign workers employed in the country’s 
                    private sector the right to change jobs without the consent 
                    of their employers. However, those desiring to change sponsors 
                    must fulfill one obligation, namely providing a three-month 
                    notice on a two-year working permit. To be sure, foreign workers 
                    still need sponsors in order to work in Bahrain, but article 
                    25 gives them the right to switch employers.<br>
                    <br>
                    As far as authorities are concerned, end of sole sponsorship 
                    of foreign workers serves the purpose of improving Bahrain’s 
                    image on the international scene. The move proved popular 
                    with international institutions caring for rights of expatriate 
                    workers. Supposedly, one such goal of the revised sponsorship 
                    system deals with improving employment prospects of locals, 
                    in turn making up less than one quarter of the workforce. 
                    Logically, the revised sponsorship regime should deny foreign 
                    workers an advantage with employers and make them compete 
                    with locals on more equal terms. It is argued that prior to 
                    revised sponsorship regime, many employers preferred expatriates 
                    over Bahraini nationals reflecting the issue of mobility. 
                    Traditionally, foreign workers had no choice but to remain 
                    with their sponsors regardless of working conditions.<br>
                    <br>
                    <b>Minimum wage</b><br>
                    The next logical step would be introduction of a minimum wage 
                    in the private sector. Existing law specifies minimum wage 
                    of $529 per month to all individuals including foreign nationals 
                    working in governmenta<img border="0" src="images/img65.jpg" align="right" hspace="6" vspace="5" width="450" height="319">l 
                    departments. However, this law primarily benefits locals, 
                    as foreign workers are a minority in government departments.<br>
                    <br>
                    Obviously, many business owners reject the minimum wage proposal 
                    for different reasons including rising operating costs. Others 
                    argue foreign workers would be the main beneficiaries of any 
                    such move by virtue of controlling three quarters of jobs 
                    in the private sector. The argument goes further that stronger 
                    earnings would only encourage foreign workers to remit more 
                    funds abroad at the cost of the local economy. <br>
                    <br>
                    Implementation of a minimum wage requirement would deny foreign 
                    workers the current advantage of accepting jobs with notoriously 
                    limited compensation. Undoubtedly, minimum wage is an international 
                    practice that cannot be overlooked. Certainly, adding minimum 
                    wage to revised sponsorship regime would shape Bahrain’s labour 
                    market. At any rate, the minimum wage could become the next 
                    battle ground between the government and the business community. 
                    <br>
                    <br>
                    To conclude, the authorities are pleased with the regional 
                    reaction to end revised sponsorship regime. In a span of days, 
                    Kuwait followed Bahrain by granting foreign workers the right 
                    to change sponsors. Other GCC countries notably Qatar could 
                    follow suit in the not too distant future. Change always needs 
                    a leader.<br>
