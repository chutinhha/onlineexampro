                      “Common Wealth” examines, digests and judges vast quantities 
                      of information from many different fields of study which 
                      bear on each of the interconnected areas of politics, economics 
                      and ecology.<br>
                      <br>
                      <strong>Managing resources</strong><br>
                      Sachs shows that there are different ways of managing the 
                      world’s technology, resources and politics from those currently 
                      being followed, and that it should be possible to adopt 
                      policies which reflect long-term and co-operative thinking 
                      instead of, as currently, disregard for others and ever-increasing 
                      barriers to solving the problems which we collectively face. 
                      It is a book which appeals equally to both head and heart, 
                      and one which no globally thinking person can ignore<br>
