                      - rice, fossil fuels, credit - to go around. Of course, 
                      we’ve been here before. In the 19th century, Thomas Malthus 
                      teased out the implications of humans reproducing more rapidly 
                      than the supply of food could grow. In 1972, the Club of 
                      Rome published to much hoopla, a book entitled “Limits to 
                      Growth.” The thesis: There are too many people and too few 
                      natural resources to go around. In 1978, it was felt that 
                      there was sufficient oil only for the next 25 to 30 years. 
                      Well, as Yogi Berra once may have said, “It’s hard to make 
                      predictions, especially about the future.”<br>
