                    </font><font face="Verdana" size="4"><b>Hedging For Turbulence</b></font><font face="Verdana" size="1"><br>
                    <b>Rezidor Hotel Group, opened the Park Inn Hotel in Muscat 
                    earlier this year and is currently looki</b><img border="0" src="images/img78.jpg" align="right" hspace="8" vspace="6" width="175" height="393"><b>ng 
                    at opening its first Missoni Hotel in Kuwait. CEO, Kurt Ritter 
                    replies to questions sent by OER<br>
                    <br>
                    The chain has achieved significant cost cutting over the last 
                    year. How have you managed this without compromising on the 
                    quality of your offering’s?</b><br>
                    We have created a special cost cutting programme which is 
                    called “Hedging for Turbulence.” This programme is not just 
                    a fancy headline, but really rules our daily business – all 
                    our brands, hotels and offices focus on controlling costs 
                    and increasing efficiencies. A lot of employees took over 
                    additional responsibilities and roles, we renegotiated purchasing 
                    contracts, and literally turned every stone in order to find 
                    new opportunities and scenarios to contain costs. At the same 
                    time we tried to maintain our unique service philosophy “Yes 
                    I Can!” and we even launched new development options such 
                    as a E-learning network. <br>
                    <br>
                    <b>Rezidor is betting on emerging markets in a big way. How 
                    do you see these markets shaping up in the future?</b><br>
                    We are focusing on Africa and Russia/CIS. Both regions are 
                    constantly improving their development – Africa has a considerable, 
                    untapped economic potential, is improving the economic structure 
                    in most countries, and offers opportunities for non-guaranteed 
                    growth. In Russia/CIS the economic growth is projected to 
                    remain ahead of Western Europe, the domestic private equity 
                    is plentiful, and numerous cities with a population of more 
                    than 500,000 lack internationally branded hotels. <br>
                    <br>
                    <b>How badly has the hospitality industry been impacted by 
                    the financial meltdown and how have you been negotiating this?</b><br>
                    I have never experienced such a financial crisis before – 
                    the hospitality industry across all regions and segments really 
                    took a nose dive. RevPAR, EBITDA, EBITDA margin and revenue 
                    went significantly down. It is difficult to judge the swine 
                    flu impact as the outbreak is just taking place right now 
                    and we cannot say yet as to how big its influence will be 
                    on our business. As I mentioned before – our “remedy” against 
                    the financial crisis is our programme “Hedging for Turbulence” 
                    which is constantly reviewed and amended in order to go through 
                    the crisis and to come out a survivor or even winner. <br>
                    <br>
                    <b>How has the Park Inn in Muscat been received and what are 
                    your plans in the region?</b><br>
                    Although it is still relatively early, the feedback so far 
                    has been extremely positive. Park Inn is a fresh, dynamic 
                    and innovative brand which fits perfectly with the emerging 
                    market of Oman, where most international brands that are present 
                    are aimed at the luxury traveller. Prior to the Park Inn arrival, 
                    mid-market choices in Oman have largely been provided by local 
                    and regional operators. Rezidor sees an excellent opportunity 
                    to expand our growth in the region by introducing Park Inn 
                    as a new, competitive, mid-market brand with appeal for both 
                    local and international travellers. We see Park Inn, as an 
                    example, fitting well within Oman’s 2020 vision for tourism. 
                    Going forward, we will be shortly launching the Park Inn brand 
                    in the UAE with the opening of Park Inn Yas Island, Abu Dhabi 
                    in time for the inaugural Abu Dhabi Formula One Grand Prix 
                    and beyond 2009, we have already announced new Park Inn hotels 
                    in Dubai, Riyadh and Makkah and continue to look for new opportunities. 
