                    </font><b><font face="Verdana" size="4">Manpower Management<br>
                    </font> </b><font face="Verdana" size="1"><b>The successful 
                    implementation of Omanisation percentages in the private sector 
                    involves modifying prevailing working atmosphere. A report 
                    by Fatma al Araimi and Visvas Paul D Karra</b><br>
                    <br>
                    In July of this year, HE Sheikh Abdullah bin Nasser bin Abdullah 
                    al Bakri, Minister of Manpower, announced th<b><font face="Verdana" size="1"><img src="images/img81.jpg" width="267" height="296" hspace="8" vspace="6" border="0" align="right"></font></b>e 
                    latest Omanisation percentag</font><b></b><font face="Verdana" size="1">es 
                    which have to be followed by the private sector. The number 
                    of Omanis working for the p</font><font face="Verdana" size="1">rivate 
                    sector is only 15 per cent of the total labour force. While 
                    there has been a remar</font><font face="Verdana" size="1">kable 
                    growth in the number of job seekers from among secondary school 
                    certificate and diploma holders, a similar growth in the number 
                    of job opportunities is glaringly absent.<br>
                    <br>
                    HE Sheikh Abdullah Bin Nasser Al Bakri, Minister of Manpower 
                    says, “Many sectors still suffer lack of national power supply 
                    in certain</font><font face="Verdana" size="1"> majors and 
                    specialties therefore we have no other option but to allow 
                    them to recruit expatrites. On the other hand, many private 
                    sector enterprises still offer poor salary scales and work 
                    incentives which also result in high turnover of the national 
                    manpower. Many candidates also do not show readiness to work 
                    anywhere in the Sultanate and prefer to work in areas close 
                    to their residence. In a bid to overcome such challenges, 
                    the ministry of manpower is coordinating with the private 
                    sector to provide Omani workers with conducive work environment 
                    and incentives. We are also making endeavours to limit the 
                    competition in the market by Omanisation of certain professions 
                    after ensuring that Omanis can take up such work.<br>
                    <br>
                    Every year, around 40,000 students graduate from Omani higher 
                    educational institutes, including universities and colleges. 
                    The number of university degree holders registered at the 
                    ministry of manpower stands at 4800 out of which 1833 (38 
                    per cent) were employed in 2008. The number of graduates in 
                    2008, amounted to 5633, out of which 2809 (49 per cent) joined 
                    the job market. Also, only 16 per cent of the teachers in 
                    the private basic education schools are Omanis compared to 
                    84.1 per cent in public schools. Therefore the question that 
                    arises is, “Can the Omani job market absorb them?” If we want 
                    to know the answer, we need to compare the number of opportunities 
                    generated by the job market.<br>
                    <br>
                    <b>Job market</b><b><font face="Verdana" size="1"><img src="images/img27.jpg" hspace="8" vspace="6" border="0" align="left"></font></b><br>
                    The number of specialised job opportuni<b></b>ties that are 
                    currently occupied by expatriates and could be taken over 
                    by the national manpower either now or in the future stands 
                    at 67,609. As per statistics recently p<b></b>ublished by 
                    the ministry of manpower, the number of national manpower 
                    employed by the private sector during the period from May 
                    2009 till June 2009 amounted to 15,995 including 484 university 
                    graduates, 585 diploma holders and 5735 secondary school certificate 
                    holders.<br>
                    <br>
                    It was observed by Raheela bint Amer Al Riyamiyah, member 
                    of State Council and chairperson of the human resources development 
                    committee that to take over these jobs, Omani students should 
                    be properly guided in their selection of studies as required 
                    by the market. A working paper was presented by Riyamiyah 
                    in this regard at the national seminar on education output 
                    and job opportunities held in June last year.<br>
                    <br>
                    “Although Omanis have shown great readiness to accept any 
                    job, we have to double our efforts to link the education programmes 
                    to the job market needs as this will help Omani students to 
                    match their skills according to the work needs,” says Dr Hilal 
                    al Hinai, corporate affairs manager at Sohar Aluminium.<br>
                    <br>
                    “With the emergence of private colleges and universities in 
                    the last few years, the scenario is beginning to change as 
                    their passouts are better equipped to meet the rigours of 
                    the job and have a positive and flexible attitude towards 
                    work,” says Pankaj Khimji, director, Khimji Ramdas.<br>
                    <br>
                    <b>Corporate responsibility</b><br>
                    Although, expatriate workers have played a major role in the 
                    development of the country in almost all walks </font><b><font face="Verdana" size="1"> 
                    </font></b><font face="Verdana" size="1">of life, the country 
                    has reached a threshold where it has become imp</font><font face="Verdana" size="1">erative 
                    to not only follow the Omanisation guidelines stipulated by 
                    the authorities but also a corporate responsibility to employ 
                    as many locals as possible. <br>
                    <br>
                    Companies like Vale, the Brazilian mining giant which is setting 
                    up a multi-million dollar pelletisation plant in Sohar, is 
                    recruiting Omanis right from scratch. Recently, Vale selected 
                    200 Omani students for a training programme to prepare them 
                    for work at the plant which will come on stream in 2010. The 
                    students have been selected to represent different specialties 
                    such as engineering, machine operators, mechanics, electronics, 
                    electrical, chemical engineering, laboratories and port management. 
                    After rigorous interviews, the company selected 134 who will 
                    take part in an 18-month training course, which kicked off 
                    in August in Oman and Brazil.<br>
                    </font><font face="Verdana" size="4"><b><br>
                    </b> </font><font face="Verdana" size="1">The training programme 
                    has been tailored for students in collaboration with Oman 
                    Marine International College, Rusayl Institute, ministry of 
                    manpower and </font><b><font face="Verdana" size="1"> </font></b><font face="Verdana" size="1">ministry 
                    of social development. According to Sergio Leite, managing 
                    director, Vale International, Representative Office, MENA, 
                    the company began the civil works in January 2009 and they 
                    have an aggressive schedule to complete the first pelletising 
                    plant in December 2010. Says Leite, “We do have a few challenges 
                    like tight deadlines to complete the first phase of the project 
                    by the end of 2010. Secondly, this i<b><img src="images/img28.jpg" hspace="8" vspace="6" border="0" align="right"></b>s 
                    the first greenfield project outside Brazil for Vale. Thirdly, 
                    it is the size of the project. Our production capacity will 
                    be 10 million tonnes per year. This is the largest size plant 
                    for Vale outside Brazil. The fourth challenge is technology 
                    implementation, as we will be using the latest technology 
                    for the plant and last but not least is the commitment towards 
                    Omanisation.” </font> 
                  <p align="justify" style="margin: 3px 10px" dir="ltr"> <font face="Verdana" size="1"><br>
                    <b>Challenges ahead</b><br>
                    “Since we are a global company, we have the confidence we 
                    can achieve our targets. And yes it is possible even with 
                    five big challenges. Oman is an important part of our strategy 
                    to be more and more closer to the market and the Omani government 
                    is a very important partner of our dream and strategy,” points 
                    out Leite.<br>
                    <br>
                    Leite says that Vale is inviting young Omanis to be part of 
                    this huge project. Beginning with a small number of around 
                    134, the company will eventually have 600 employees. But the 
                    Omani employees have to prove their mettle within a short 
                    time by showing their values and their capabilities. They 
                    have to prove themselves right from the beginning. And they 
                    have to be prepared to give their best performance. There 
                    is no concession in this regard, Leite adds.<br>
                    <br>
                    <b>Change in work culture</b><br>
                    The rate of attrition is very high in the private sector making 
                    it very difficult for companies to retain talent. Moreover, 
                    a number of large companies have realised that apart from 
                    pushing in the numbers to reach the mandatory Omanisation 
                    levels, it is also important to manage the existing talent 
                    and prevent a general exodus.<br>
                    <br>
                    The<b><img src="images/img30.jpg" hspace="8" vspace="6" border="0" align="left"></b> Zubair Corporation has recently increased its focus and 
                    resources on developing its talen<b></b>t. Says Maryam Ahmed 
                    Hussain Al Shaibani, Group Talent Manager, “Talent Management 
                    is the systematic attraction, identification, development, 
                    engagement, retention, and deployment of individuals who are 
                    of particular value<b></b> to an organisation either in view 
                    of their high potential for the future or because they are 
                    fulfilling business/ operation-critical roles.”<b></b><br>
                    <br>
                    According to Maryam, Talent Management 
                    is a means of enhancing employee engagement to improve retention 
                    and as well as enhance the organisation’s image and supports 
                    the employer’s br<b></b>anding in the labour market. The Zubair 
                    Corporation has more than 2600 employees in all their wholly 
                    owned companies and has achieved the stipulated Omanisation 
                    targets<b></b> across all the grou<b></b>p companies but is 
                    increasing the focus on developing talent to create a future 
                    leadership pipeline.<br>
                    <br>
                    Eric McLean, Chief Development Officer, who is the overall 
                    </font><b><font face="Verdana" size="1"> </font></b><font face="Verdana" size="1">head 
                    of all <b></b>human resources in <b></b>The <b> </b>Zubair 
                    Corporation, says that changing the work culture and the way 
                    we manage talent is the most important thing for every company. 
                    Giving an analogy, he says that every company has many uncut 
                    diamonds – those employees whose potential is not yet realised. 
                    They are your assets and you need to look after them by identifying 
                    where they are; assessing their potential; developing them 
                    correctly and placing them in the right setting.<br>
                    </font><font face="Verdana" size="1"><br>
                    Talking of employee engagement in a company, McLean says there 
                    are three kinds of people in an organisation. He calls the 
                    first type as ‘completely engaged’ and they work as if the 
                    company is their own. They care and are empowered. The second 
                    are the ‘not-engaged’ category, th<b><img src="images/img29.jpg" hspace="8" vspace="6" border="0" align="right"></b>ese 
                    are 9-5 workers who do their job and go home; and the third 
                    type are the ‘active disengaged’. You need to get as many 
                    people as possible into the first category, points out McLean. 
                    Looking after your talent properly helps engage them into 
                    the first category.<br>
                    <br>
                    Says Khimji, “Our group is on an expansion mode and we have 
                    enough scope for both Omanis as well as expatriates to work 
                    and grow. We have a robust training and skill development 
                    programme in place. For example, we send our brightest young 
                    Omani talent to India for an intensive development programme 
                    in a leading professional institute besides hands on job experience 
                    in the top notch companies to understand and learn the best 
                    work practices.” The corporate sector is serious about promoting 
                    job opportunities <br>
