                    </font><font face="Verdana" size="4"><b>On the prowl<br>
				</b></font><font face="Verdana" size="1"><b>Swift and timely 
				action by regulatory authorities unearthed fake funds and saved 
				gullible investors from burning their fingers. Mayank Singh 
				reports</b><br>
				<br>
				The Public Prosecution Department recently busted a fake 
				Platinum Portfolio fund operating as the Al Naqd Investment 
				Company. Operating without a proper license, the fund had 
				promised eight to ten per cent returns every month to investors. 
				The ruse wor<strong style="font-weight: 400"><img border="0" src="images/img8.jpg" align="right" hspace="7" vspace="7"></strong>ked and the promoters managed to attract RO7mn from 
				gullible investors. The money was then diverted for personal 
				expenses of the promoters like the purchase of a residential 
				property and other luxuries. The Platinum portfolio came on the 
				heels of other such frauds like the Mine and Diamond financial 
				portfolios that had fleeced investors similarly. Says Ali 
				Mohammed Juma, CEO, Vision Investment Services, “Almost every 
				ten years, we find people claiming that they can give very high 
				returns and almost all of them turn out to be scandals.” <br>
				<br>
				<b>Taking cognisance </b><br>
				The regulatory authorities on their part are vigilant about such 
				efforts. Sharekhan, an India based brokerage company was 
				recently found to be operating in Muscat, without the requisite 
				regulatory approvals and stern action was taken against it. 
				Subsequently, the Capital Market Authority (CMA) ran an 
				advertisement in newspapers listing out the 22 companies that 
				have a license to offer asset management and portfolio 
				management services in the Sultanate. Says Abdullah Salem al 
				Salmi, executive vice president, CMA, “There have been instances 
				when the authorities have found funds or companies collecting 
				money from the public without a license. We investigated and 
				carried out operations against such companies jointly with the 
				public prosecutor and the Royal Oman Police, and some of them 
				have been convicted.” Says Juma, “One cannot over emphasise the 
				importance of regulatory bodies in any economy to check such 
				malpractices.”<br>
				<br>
				Apart from regulatory deterrence it is important to educate 
				customers. For example, investors need to check the credentials 
				of a fund or company before making an investment. They should 
				also have reasonable expectations about the expected returns and 
				not get swayed by unrealistic promises. The CMA and other 
				investment companies are playing an important role in 
				disseminating information about sound investment practices. The 
				CMA for example has a special department which conducts 
				workshops and seminars to educate investors.<br>
				<br>
				<b>Education drive</b><br>
				Ponzi schemes and financial rackets are found in even the most 
				advanced countries. The recent Bernard Madoff scam in the US saw 
				investors losing close to $65bn and it is being billed as one of 
				the biggest scams in the history of financial fraud. Madoff’s 
				victims included not just the hoi polloi but celebrity names 
				like film producer Steven Spielberg and Hollywood actor Kevin 
				Bacon. So what lures intelligent and educated people to such 
				schemes? Says Juma, “There are various motives like greed, 
				ignorance and psychology of the masses which sways even the most 
				well heeled.”<br>
				<br>
				While high net worth individuals and big companies have the 
				expertise to differentiate between sound investment options and 
				fly-by-night operators, a number of naive customers get cheated. 
				“Oman with a young population and lifestyle trends puts a lot of 
				pressure on youngsters and middle age people to get rich soon, 
				which substantially increases their risk profile,” says Juma. A 
				good way of creating wealth is to follow financial planning over 
				a period of time and knowing the difference between gambling and 
				sound financial planning.<br>
