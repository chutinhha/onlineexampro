				<br>
				During the course of an interview for this issue Jan Babiak, 
				partner, global climate change &amp; sustainability services leader, 
				Ernst&amp;Young said, “In technology you can never be sure that you 
				know everything or say that I am the expert and come to me, as 
				there is always something new; it might be in a different 
				industry or a different geography, it may be the shift from 
				mainframes to mini computers to micros to the dot com world to 
				cloud computing.” Prior to her current role, Babiak was a 
				pioneer in the field of technology leading E&amp;Y’s global practice 
				providing IT security, programme management and assurance 
				services and authoring Defending the Digital Frontier, a book 
				focused on helping board members prepare for cyber threats.<br>
				<br>
				Point taken – technology is the biggest change agent in today’s 
				world and this change is taking place at a breathless pace. What 
				is in vogue today is a dinosaur tomorrow. Companies which can 
				harness this revolution become the darling of investors and 
				shareholders overnight. A recent study placed the brand value of 
				Google at $100bn. So if someone wanted to buy merely the brand 
				without any of its assets, he would have to cough up that 
				amount.<br>
				<br>
				IT preparedness applies not just to companies but also for 
				countries. And there are indices to measure the same. In a 
				recent Global IT Report 2008-09 featuring 134 countries 
				published by the World Economic Forum the Sultanate was ranked 
				50th in the overall report and 39th on the government readiness 
				index. Similarly, Oman is ranked 84th in the UN E-Government 
				Readiness Index 2008, a jump of 28 points from 2005.<br>
				<br>
				The Sultanate’s success can be traced back to 2002 when a 
				Digital Oman strategy was formulated. Setting up of the 
				Information Technology Authority (ITA) in 2006 gave wings to 
				that vision. The ITA has been instrumental in fostering an IT 
				savvy culture in Oman. It has also been in the vanguard of a 
				number of initiatives like the National ID card, ePayment 
				gateway, ePurse, etc. Others like the Royal Oman Police, Muscat 
				Municipality, Ministry of Commerce and Industry and the Ministry 
				of Higher Education have followed suit with their individual 
				initiatives. Our cover story looks at how the application of IT 
				is altering a few things – the way we do business; park our 
				cars; carry out transactions; apply for licenses; pay bills; 
				seek admission to colleges; buy and sell things. Phew! let me 
				rephrase it – how IT is changing our lives.<br>
