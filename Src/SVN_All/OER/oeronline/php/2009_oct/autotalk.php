                    in and are confronted by two SUVs, the X5 and the X6. On paper 
                    both are somewhat similar aside from the Rear Design, power 
                    ratings and performance figures and the X6 being slightly 
                    pricier. So which would you choose? Malcolm Xavier Crasta 
                    gives a lowdown</b></font></p>
                  <p align="justify" style="margin-left: 10px; margin-right: 10px"> 
                    <font face="Verdana" size="1"> <img border="0" src="images/img51.jpg" align="right" vspace="5" hspace="8" style="border: 1px solid #E9E9E9">On 
                    first impression the X6, quite confusingly named X6 xDrive50i, 
                    seems somewhat overshadowed by the X5. While the X6 seems 
                    sportier, the X5 has a lot more space, is cheaper and more 
                    practical. We have driven the X5, it is no slouch and we had 
                    no problems with its ride and handling. In addition the X6’s 
                    looks are a love-hate affair, you either love it or you hate 
                    it, it is extremely hard to find any middle ground. Despite 
                    this, it is different enough to set itself apart from the 
                    regular SUV crowd thanks in part to the fastback roofline, 
                    huge wheels and sporty cues. Even on the inside you get nothing 
                    particularly special with the X6, which shares nearly every 
                    bit of its layout and high quality fittings with its other 
                    SUV counterpart. So all things considered, an X5 should be 
                    a no-brainer… that is until you sink into the driver’s seat 
                    of the X6, start it up and step on the gas.<br>
                    <br>
                    For starters the seats themselves are sportier and subsequently 
                    have a great driving position. And along with the deeply raked 
                    front windscreen and great forward visibility truly gives 
                    it a sports car feel. Unfortunately, there is barely any visibility 
                    through the back thanks to the narrow rear window line and 
                    high back; at least it further accentuates the sportiness 
                    of the vehicle. For the most part you won’t really need the 
                    rear visibility unless you enjoy watching other cars disappear 
                    into the horizon because this is one fast beast.<br>
                    <br>
                    Powered by a 4.4-litre twin-turbo V8 the X6 has some serious 
                    power under the bonnet. This gem of an engine produces 407hp 
                    and a colossal 600Nm of torque, which is available as early 
                    as 1,750 rpm. The end result – you get a nearly 2.3 tonne 
                    vehicle that can hit 100kmph in just 5.4 seconds, this is 
                    sports car territory. Once in full clip there are very few 
                    cars that can even keep up with it. In fact, at first it can 
                    actually be quite intimidating to see something this big and 
                    heavy move this fast.<br>
                    <br>
                    <img src="images/img54.jpg" hspace="8" vspace="5" border="0" align="left" style="border: 1px solid #E9E9E9"> 
                    The X6 doesn’t just impress in a straight line, if anything 
                    it is even more impressive in the bends. In effect it is a 
                    nimbler version of the X5, but the technology behind it goes 
                    a bit deeper. The X6 features an all-wheel drive system with 
                    a newly-developed and extremely impressive torque distribution 
                    system, aptly named Dynamic Performance Control. In essence 
                    the system constantly controls the distribution of torque 
                    that is being transferred to each of the wheels when cornering. 
                    In doing so the system is able to make each tyre perform to 
                    its maximum efficiency and provide just the right amount of 
                    grip and directional stability that the situation demands. 
                    Whatever the technicalities may be the end result is what 
                    matters and in this case it is, for lack of better words, 
                    simply amazing. What you end up with is by far the best handling 
                    SUV on the market today and it does this without even sacrificing 
                    any driver feedback. <br>
                    <br>
                    On the highway everything is standard BMW fare. The ride is 
                    complacent and sound levels are kept to a bare minimum aside 
                    from the rumble of the engine, which is not something you 
                    will ever get tired of hearing. And aside from some shortage 
                    of headroom and the availability of only two seats, the back 
                    of the X6 is also quite a snug place to be seated on long 
                    drives, especially so if the rear DVD system is fitted. Toys 
                    abound up-front as well with BMW’s iDrive controls and the 
                    6-speed gear shifter taking centre stage. You get a DVD changer, 
                    HiFi loudspeaker system, fully electric seats with memory, 
                    rear view camera and a lot more, there is even a nifty heads-up 
                    display that you find in other high-end BMWs.<br>
                    <br>
                    All in all the X6 should be at the top of your list if you 
                    are looking for an all weather vehicle with seating for four 
                    and sports car performance. And then again if you want more 
                    practicality there is always the X5. In producing the X6 BMW 
                    has created a niche for itself that very few vehicles can 
                    compete in the Sports Activity Coupe as BMW fittingly calls 
                    it.<br>
