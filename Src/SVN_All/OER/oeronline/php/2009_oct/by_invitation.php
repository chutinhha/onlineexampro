				<b><font face="Verdana" size="4"><br>
                    Road to economic recovery</font><font face="Verdana" size="1" color="#CC3300"><br>
                    </font><font face="Verdana" size="1"><img border="0" src="images/img61.jpg" align="right" hspace="8" vspace="4" width="100" height="125">The 
                    bouncing of the stock market, rise in oil prices and the completion 
                    of many real estate projects in Oman means that the Sultanate 
                    is on the road to recovery<font face="Verdana" size="2" color="#CC3300"><br>
                    </font></font></b><font face="Verdana" size="1"><br>
                    At the beginning of this year, world economic leaders and 
                    financial analysts were painting a very gloomy picture about 
                    the state of the world economy. They had warned of a global 
                    recession like the Great Depression. Thankfully, the reality 
                    has fallen substantially short of their predictions. There 
                    are encouraging signs that economic prospects, which seemed 
                    dire as the financial crisis deepened, might be improving.<br>
                    <br>
                    The Organisation for Economic Co-operation and Development 
                    (OECD) has now predicted that the global economy will emerge 
                    from the downturn faster than previously thought. The extraordinary 
                    steps taken by governments and central banks since the ‘great 
                    panic’ of September 2008 – the bank bail-outs, the record 
                    interest rate cuts, the trillions of dollars in budget stimulus 
                    – all seems to have worked.<br>
                    <br>
                    Most major economies have registered growth between April 
                    and June 2009. Though this may not be so true for the UK or 
                    US, the two countries at the centre of the financial crisis, 
                    but France, Germany and Japan all supposedly <br>
                    grew in the second quarter of 2009.<br>
                    <br>
                    <b>Economic correction</b><br>
                    So what about Oman’s road to economic recovery? Oman would 
                    have been a showcase of a country that was immune to the global 
                    meltdown, had it not been for the unprecedented growth which 
                    began immediately after Cyclone Gonu in June 2007. Major government 
                    infrastructure repair and development projects, real estate 
                    development and tourism projects were being undertaken. There 
                    was a huge inflow of cash from the other Gulf countries into 
                    the stock market and the real estate market. The country was 
                    not well prepared for such a sudden unplanned growth.<br>
                    <br>
                    My view, which may not be shared by others, is that the economic 
                    downturn we experienced in Oman was mostly a correction bringing 
                    us back to more or less a normal economic growth. Therefore, 
                    when we talk of economic recovery, we may not necessarily 
                    mean recovery to the mid 2008 level but rather achieving reasonable 
                    growth from the bottom we reached at the end of 2008. Let 
                    us examine the major economic barometers to see if we are 
                    on the road to recovery.<br>
                    <br>
                    <b>Barometer rising</b><br>
                    The Muscat Securities 
                    Market (MSM) has been seeing some recovery in the second and 
                    third quarter of his year. The MSM 30 index opened at the 
                    start of the year at about 4,500 points and has at present 
                    reached 6,500 points – a 44 per cent increase. To put this 
                    in perspective, th<b><img border="0" src="images/img62.jpg" align="left" hspace="8" vspace="4" width="150" height="190"></b>e 
                    index reached its highest level of 12,100 points in June 2008. 
                    I don’t expect the index to reach its previous peak anytime 
                    soon but the recovery is quite noticeable.<br>
                    <br>
                    Our economy is still oil driven and oil price movements have 
                    a great impact on the direction of our economy. Before the 
                    crisis, oil price was above $100 per barrel in October 2008 
                    which dropped to its lowest level at $38. Now the prices have 
                    touched $70 and analysts are predicting that it is likely 
                    to stay at the current level at least till the end of this 
                    year. The increase in price of oil lately has been associated 
                    with the cheaper dollar. With the rial pegged to the dollar, 
                    Oman will not get the full benefit of oil price rise, if the 
                    value of dollar goes down. The oil price at the current level 
                    together with increased investor confidence has resulted in 
                    increased spending by the government and private sector on 
                    various projects which inturn creates job opportunities and 
                    increased consumer spending. It is comforting to note that 
                    inspite of the signs of recovery, inflation has reportedly 
                    come down to 1.8 per cent in August 2009 from an 18 year high 
                    of 12 per cent in February 2008.<br>
                    <br>
                    Real estate sector was hit badly by the economic slowdown 
                    and recovery has been slow. Projects that were in progress 
                    when the crisis unfolded in mid-2008 have not stopped. Driving 
                    along the highway in the capital area one cannot miss the 
                    big banners on new buildings advertising availability of commercial 
                    and residential property. As there has been a shortage of 
                    both commercial and residential space, this will be a welcome 
                    relief. But a senior official of a reputed real estate company 
                    recently agreed that these big banners may not be helping 
                    to inspire confidence.<br>
                    <br>
                    <b>Apprehensive banks</b><br>
                    One area, which is still lagging behind on the road to recovery 
                    is bank lending, a problem experienced by most economies of 
                    the world. Recently, the French government directed its banks 
                    to start lending again to small and medium enterprises (SMEs). 
                    Presently, SMEs are suffering from lack of credit facilities 
                    from lending institutions. With our economy predominantly 
                    being SME driven, the authorities need to find ways to help 
                    them in getting credit facilities. In conclusion, there are 
                    strong signs that Oman is on its way to economic recovery. 
                    Historically, investment decisions in this country usually 
                    start to happen in September/October when the summer holidays 
                    are over and investors start taking a fresh look and plan 
                    for the new year. Hence, we should expect the year to end 
                    with positive prospects, however, we should not forget to 
                    take lessons from the crisis. <br>
