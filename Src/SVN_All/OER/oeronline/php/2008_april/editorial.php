</font><b><font face="Verdana" size="4">‘Healthy’ Prospects editorial </font>
</b><font face="Verdana" size="1"><br>
<br>
The original purport of the adage, ‘health is wealth’, is to reiterate the 
importance of remaining healthy. Of late, this is assuming new meanings, 
particularly in the context of the Sultanate of Oman. While neighbouring Dubai 
is fast-tracking Dubai Medical City to be up and ready for a 2010 launch, Oman 
sees the potential to emerge as yet another destination for medical tourism. A 
healthcare city, at an estimated cost of US$1-billion, near Muscat has become 
the latest talking point, being orchestrated by the Kuwait-based Gulf Investment 
House. While a feasibility study is underway, the Ministry of Tourism has 
green-flagged its blessings with a land allotment offer of one million sq m near 
the Blue City.<br>
<br>
Oman offers the choicest leisure activities in terms of virgin, unpolluted 
beaches and seafronts, adventure sports, such as wadi-bashing (on a 4WD mode), 
scuba diving, snorkelling, mountain trekking, etc. Throw in medicare, with 
state-of-the-art facilities in place at affordable price for the needy, 
specially from the US and Europe where healthcare costs are steep, then the 
business proposition is appealing. That’s what potential investors like GIH are 
looking for.<br>
<br>
It is no secret that foreigners – middle aged and senior citizens – flock to the 
Sultanate to escape biting cold in Europe and enjoy its panoramic landscape. A 
rejuvenation trip, no doubt.<br>
<br>
For a moment, leave alone visitors from abroad. According to reports, a decade 
back, medical tourism was hardly large enough to be noticed. Today, more than 
250,000 patients per year visit Singapore alone, nearly half of them from the 
Middle East. Oman can use the tourism-cum-healthcare combo as a pull factor 
besides the ‘neighbourhood’ factor for the needy from the region.<br>
<br>
Moreover, Oman is regarded as having one of the most efficient healthcare 
systems, not only in the Middle East and North Africa (MENA) region but globally 
as well, with a recent study by the World Health Organisation judging the 
country’s healthcare system best in terms of outcome. What more one can vouch 
for?<br>
In fact, the growth of country’s pharmaceutical market is assured in the short 
to medium term, supported by excellent healthcare facilities, which are 
continuing to receive attention from foreign investors. The exclusion of 
expatriate workers from public healthcare schemes is also favouring the growth 
of high quality private healthcare centres. Also, accessing top-notch healthcare 
practitioners from across the globe on consultancy basis, with best onground 
professionals, can make Oman the most affordable option for healthcare seekers.<br>
<br>
The proximity to India, another medical tourism magnet in the neighbourhood, can 
also have a positive fall-out, with potential patients looking at Oman as 
another choice for recovery and rejuvenation at affordable cost. At any rate, 
the Oman project will make a lesser dent on patients’ pocket than the Dubai 
version. The biggest feather in Oman’s cap is its changed intellectual property 
environment. Affordable healthcare with tourism thrown in is thus another growth 
story that the Sultanate must script.<br>
