</font><b><font face="Verdana" size="4">Paving the way for Power Women</font></b><font face="Verdana" size="1"><br>
<b>The recently concluded Women in Business Conference in Muscat brought 
together women leaders from across the world to encourage and empower female 
entrepreneurship. Nathalia Jones reports<br>
</b><br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
<img border="0" src="inthenews.jpg" width="420" height="231"><br>
<br>
<br>
Omani women are coming into their own, shedding traditional roles and 
increasingly moving into business spaces, which previously were primarily a 
patriarchal domain. As opposed to earlier when women in the Sultanate were bound 
to the home and family, today they’ve etched a name for themselves in virtually 
all spheres of economy and business – they are important decision-makers in the 
government and planning organisations; they’ve landed key positions in 
management; some of them play pivotal social roles in effecting positive 
thinking and progress; and still some are striking it out on their own in the 
corporate world.<br>
<br>
Statistics from the Ministry of National Economy amply support this development 
showing that gender differentials in education and literacy levels are fast 
disappearing and there is now gender parity in access to all levels of 
education.<br>
<br>
Female education specially is one area where Oman has made giant strides and it 
has spurred the country’s female population on to greater achievements. Apart 
from having achieved a female literacy rate of 49 per cent as per 2006-07 
statistics from the Ministry of National Economy.<br>
<br>
Although statistics on women’s advancement in Oman are indeed heartening, they 
also provide evidence of a very one dimensional development, with women’s 
presence in various sectors of business and enterprise still at a nascent stage, 
even as the inspiration and initiative is there. For instance, statistics point 
out that the percentage of females in the public sector was just 31 per cent two 
years ago, while the percentage of the female workforce in the private sector 
was even lower at only 17.9 per cent. What’s more, only 2 per cent of Omani 
women were self-employed and they constituted less than 1 per cent of Omani 
employers.<br>
<br>
When it comes to business enterprises and self-initiated ventures, the trend 
among Omani women seems to be in the direction of cosmetic products and beauty 
salons, and other small-scale operations. However, there is a whole new world of 
opportunity emerging in Oman which they need to explore and exploit. And it was 
this objective that received a unanimous vote at the recently held Women in 
Business Conference in Muscat. OER was the strategic partner of the event.<br>
<br>
“I think businesses are like plants: they either grow or die. And I think women 
particularly in the UK tend to set up businesses at home that often don’t move 
out of the home. They often tend to be self-employed; I don’t think there’s 
anything wrong with that, but personally for me the most exciting thing about 
business is the growth; its how you change lives in supplying excellent service; 
its where business gets fun for me when it grows,” said Liz Jackson one of the 
many motivational speakers at the conference, a two-day event held under the 
auspices of HH Sayyida Alia bint Thuwaini Al Said.<br>
<br>
The event saw participation from women entrepreneurs and prominent businesswomen 
from all over the world, who shared inspiration and knowledge through their 
experiences in the corporate arena. It was a platform for networking and an 
opportunity for women of the Sultanate to imbibe core business values and 
principles for running a successful enterprise.<br>
<br>
From luxury brands to women’s ever changing role in society and the economy, to 
setting goals and cultivating essential skills, the speakers delivered messages 
that encouraged and inspired, even as they motivated and sparked resolute 
determination in the minds and hearts of the largely female audience.<br>
<br>
Workshops and seminars during the event addressed key issues regarding women 
entrepreneurs and these were particularly relevant to the Arab world where women 
are still having to resist being bracketed into conventional roles as mother, 
wife and home maker. Apart from international business speakers, who offered a 
global perspective and a broader experience, Oman’s own home-grown power women 
like Huda Al-Habsi, head of marketing and product development at Oman 
Telecommunications Company, held their own, encouraging women to take up 
leadership positions and step out in confidence.<br>
<br>
In any business communication is key, Sally Burns, the secondary ESL teacher at 
ABA – An IB World School, presented a paper titled – ‘Two ears and one mouth – 
Effective communication at work’, which emphasised the power of silence and the 
role of listening in communication at work. Participants also gained useful 
insights into the kind of goals they need to set for themselves when starting a 
business, what strategies they need to deploy to sustain their corporate 
ventures and how best to make use of market potential.</font></p>
