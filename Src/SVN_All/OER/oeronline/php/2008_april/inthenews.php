		</font><font face="Verdana" size="2"><b>‘There are no real barriers to success’</b></font><font face="Verdana" size="1"><br>
<br>
When Liz Jackson walks into the boardroom, one of the things she tells her 
audience before getting down to business is this, “By the way, I’m blind so I’ll 
be holding on to someone’s arm as I move through your office…. If I don’t make 
good eye contact, don’t blame me; I’m aiming for where I think your eyes are”. 
Well, she could’ve fooled anyone, for when she speaks her gaze meets your’s 
head-on, unwavering, direct and honest. Her gait is as sure-footed, with not 
even the slightest hint of stumbling hesitancy, the sign of an iron will and 
resolute determination to, as she says, ‘go out there and just do it’. And 
that’s exactly the message that Liz had for the Sultanate’s aspiring women 
entrepreneurs during the second annual Women in Business Conference held 
recently.<br>
<br>
Her story is one of success against all odds, and at least for Liz being 
visually impaired was hardly the biggest of her problems. She does admit that it 
was a ‘bit of a blow’ to discover her eyesight was deteriorating rapidly. 
However, the larger issue was finding enough financial support for her 
initiative. Her request for a loan was turned down by banks on grounds that she 
didn’t have enough assets that would serve as collateral in case the business 
went down, which they expected it would. They did, however, point her in the 
direction of the Prince’s Youth Trust which whole-heartedly supported her idea 
and provided her a grant of £1,000 and a loan advancement of £4,000. Great Guns 
Marketing was launched and today the company has a turnover of more than £2.5 
million, growing at a rate of over 40 per cent per annum, with eight regional 
branches.<br>
<br>
When Liz told her story at the conference, she was actually telling other women 
with similar dreams and ideals that they too could do it. And now she shares a 
few more insights in an interview with Oman Economic Review. Excerpts:<br>
<br>
		<b>What is it about business that drives you?</b><br>
What I really enjoy is a fast-growing business; one which is all about winning 
clients. It’s all that which put the fire in my belly to start Great Guns 
Marketing. Personally, for me, the most exciting thing about business is the 
growth; its how you change lives while supplying excellent service; its where 
business gets fun for growing lives. It’s very exciting working with a start-up 
company, seeing them making their first load of appointments and watching them 
win business and grow.<br>
		<br>
		<b>What exactly do you tell people at conferences?</b><br>
I am trying to inspire people if they’ve already got businesses to try and grow 
them. I think businesses are like plants, they either grow or die and I think 
women, certainly in the UK, they tend to set up business at home and they often 
tend to be self-employed. I don’t think there’s anything wrong with that but you 
need to expand. So, I was trying to inspire them to do this; to sort of pull 
their finger out and get on with it and take action.<br>
<br>
		<b>What are the challenges women face as budding entrepreneurs?</b><br>
With regard to Arab woman, I don’t think I’ve been in any of these places long 
enough to understand the culture as well as I’d like to. But certainly at first 
glance, it looks exciting and the challenges are often quite similar. I mean 
women in business in the UK are being encouraged by our government to set up and 
run companies but we certainly don’t set up and run nearly as many as our male 
counterparts. And I think the challenges here are pretty similar but it sounds 
like that the governments here are seem to be really encouraging women and 
seeing the value in men and women being key 
players in the economy.<br>
