<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
</font><b><font face="Verdana" size="4">Bear Hug, Temporary?<br>
</font></b><font face="Verdana" size="1"><b>Slumping stocks and a sliding dollar 
have been generating anxiety among executives and policy makers in early 2008<br>
</b><br>
“If Bear Stearns goes bust in the US, why should my investment here get 
affected? The companies where I have invested are robust. Still they have been 
rocked. Mine – rather your money – has evaporated drastically. Tell me, should I 
pull out? Or what?” This was my better half on the e-mail in mid-March – a day 
after the Federal Reserve bailed out the ailing Bear Stearns with a whopping 
US$30 billion package through JP Morgan Chase to stem the financial market 
crisis. A big rescue act to prevent a global collapse. Thanks, Uncle Sam!<br>
<br>
<img border="0" src="viewpoint.gif" width="133" height="209" align="left" hspace="3" vspace="2">Dubai-based Michael Preiss, a private investment banker working for a 
multinational banking conglomerate and a friend, was stiff and taciturn on phone 
when I buzzed him for gaining an insight: ‘Look, I am neck deep in trouble with 
the market collapse. Can we catch up some other time?’ A small time investor 
like my wife is in a financial soup. So is a big time financial wizard like 
Preiss.<br>
<br>
When things get out of control, it is but natural for us to panic. Maintaining a 
stoic posture in such situations is considered unnatural. After all, we are a 
bundle of emotions. Like it or not, we have been structured that way. I quickly 
did a back-of-the-envelope calculation and found unsurprisingly that my 
investments had shrunk by a whopping 40 per cent in the past six months! Without 
sweat, I responded with a “cool” message to my wife’s SOS. What else one can do 
under such circumstances?<br>
<br>
<b>Learn to live</b><br>
On second thoughts, I felt comfortable for two reasons: primarily, I am 
convinced such wild fluctuations are part and parcel of capital market history. 
If indices go down in one season, they are bound to climb up in another. But, 
how soon will this climb happen. There is no answer to this question, honestly. 
The need of the hour is: patience. Did not the world’s richest man and 
investment legend, Warren Buffett, claim that ‘patience is a virtue’? Secondly – 
actually this ought to have been the primary reason! – I was not alone in the 
financial whirlpool gripping the world. There are millions, spread across 
continents, all in the same soup! <br>
<br>
Anyways, I was in a fix. Temporarily though. Why was I not getting into a foul 
mood and jumping up and down? Global consultancy guru McKinsey was my 
palliative. “Slumping stocks and a sliding dollar have been generating anxiety 
among executives and policy makers in early 2008. Amid the turmoil, it’s easy to 
forget that long-term structural change in the world’s capital markets will 
probably prove more important than short-term fluctuations, as it did after the 
1987 US stock market crash, the 1992 assault on the British Pound, and the 1997 
unravelling of Asia’s financial markets,” avers the February 2008 issues of The 
McKinsey Quarterly while dissecting ‘Long-term trends in the global capital 
market’.<br>
<br>
<b>What’s happening?</b><br>
Briefly put, the world has erupted. Want proof? Here it is. Over the past 
quarter century, financial assets have multiplied by 17 per cent between 2005 
and 2006. In absolute numbers, the value of financial assets stood at US$167 
trillion as against US$142 trillion the year before. You will understand the 
impact when you are told that the average annual growth was 8 per cent between 
1995 and 2005!<br>
<br>
Another interesting stat to mull over. The McKinsey Global Institute study 
points out financial markets have been growing faster than global GDP for many 
years. Hardly 20 years ago, there were 33 countries whose value of financial 
assets exceeded their respective GDPs. In 2006, the tally is 72. Significantly, 
BRIC countries’ (read the emerging markets) GNP has been left behind their 
respective financial assets value. No wonder, they are labelled booming emerging 
markets! Here’s the cherry: in 1990, there were only two countries whose 
financial depth exceeded 300 per cent. Today, there are 26.<br>
<br>
<b>Why is so much money sloshing?<br>
</b>Elementary, my dear Watson! There are more financial instruments available to 
park your savings. The privatisation of state-owned companies across the globe 
is one element. Asset-backed securities is another. New savings products is one 
more dimension. Rising asset prices and government debt can also add to the 
depth of financial assets, if McKinsey is to be believed. Above all, the global 
economy – collectively – was in a fine nick throughout the 1990s and till 2006 
before the economic jitters begun to unsettle the US economy with recessionary 
fears and the subprime fall out. Cross border investments (CBI) are yet another 
dimension. At the end of 2006, for which concise data is available, the 
outstanding stock of cross border investment in real terms stood at US$74.5 
trillion, which includes foreign investment by MNCs, purchases of foreign debts 
and equity by investors, and foreign lending and deposits. And since 2006, these 
would have only added more value. It is not out of place here to indicate that 
the value of petrodollar foreign investments was pegged at US$3.8 trillion in 
end 2006.<br>
<br>
Oman, for instance, attracted RO5.9 billion in 2006 as against RO4.07 billion 
the previous year, according to a survey conducted last year by the country’s 
ministries of national economy and commerce and industry, and the Central Bank 
of Oman (CBO). The United Kingdom led the table, followed by the UAE and the US. 
Simultaneously, Omani direct foreign investments abroad constituted 18 per cent 
of total Omani investment abroad in 2006, which was RO584 million. The rising 
level of foreign investment across regions is making the world more financially 
interdependent than it was in the recent past. So, the entire world catches 
cold, if the United States sneezes. It is inevitable. Hence, we have to pray for 
collective prosperity. And, focus on the long term. Ignore, here, the 
much-abused Keynesian quote: in the long term, we are all dead! <br>
