        &nbsp;<p align="justify" style="margin: 3px 10px">
        <font size="1" face="Verdana">It is hardly a few days to 
        elapse before Circa 2007 bids goodbye ushering in the New Year and 
        therefore, it is time for a re-look at what transpired for the Sultanate 
        of Oman. Heartening it is to note that, Oman has performed extremely 
        well in almost all sectors of the economy, despite a few hiccups: 
        Cyclone Gonu for instance that a wee bit slowed down the process, but 
        the resilience shown by the residents of the Sultanate – natives and 
        expats – to regain the momentum is remarkable.<br>
        <br>
        OMAN 2007, the bilingual annual in English and Arabic, is out for the 
        third consecutive year showcasing the developments that is shaping the 
        future of the Sultanate under His Majesty Sultan bin Said Qaboo’s 
        suzerainty and stewardship. <br>
        <br>
        The stunningly beautiful book, brought out by the United Media Services, 
        rich in content and visual delight covers areas such as: Agriculture, 
        Automotive, Aviation, Banking, Capital Market, Foreign Relations, Health 
        and Education, Infrastructure, Insurance, Manufacturing, Oil &amp; Gas, Real 
        Estate, Tourism, Women Empowerment, Regional Development etc. Ministers, 
        Government Officials and experts from relevant domains have contributed 
        to further embellish OMAN 2007. OMAN 2007 is a ready-reckoner for a 
        first time visitor to understand the economic dynamics of the Sultanate. 
        What it is not? It just does not harp on the past glories alone, but 
        turns the searchlight in what is happening on ground that will transform 
        the shape of the Sultanate in the years to come. Because, it should be 
        remembered that Oman is the most dynamic economy in the region that is 
        pursuing a more clearheaded diversification charter to move away from 
        the oil dependency syndrome. <br>
