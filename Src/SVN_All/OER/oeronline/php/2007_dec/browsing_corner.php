        <p align="justify"><b><font face="Verdana" size="4"><br>
        How Does the Boss Behave?</font></b><font face="Verdana" size="1"><br>
        <b>Marshall Goldsmith outlines attitudes and habits that are an anathema 
        to good leadership</b><br>
        <br>
        <img border="0" src="browseingcorner.jpg" align="left" hspace="8" vspace="4">Managing people in work circumstances has always been an art. At the end 
        of the day, it is often observed that idiosyncrasies mar many 
        near-perfect approaches to well-oiled people systems that have the 
        makings of very high productivity, but are corrupted by an individual’s 
        attitude.<br>
        <br>
        Marshall Goldsmith has captured the essence of this facet that causes 
        turbulence in interpersonal relations. In What got you here won’t get 
        you there, a wide spectrum of attitudes and behaviour situations are 
        capsulated into 20 faulty habits. What’s often not understood by the 
        boss is that turmoil with even one subordinate spreads to the rest of 
        the team.<br>
        <br>
        Having dealt with 20 boss-habits that dampen employee morale and 
        enthusiasm in the first part of the book, Goldsmith ends the section 
        with that larger-than-life obsession many bosses have – goal ambition. 
        All in all, the total concept of misplaced personal value systems 
        influencing workplace behaviour is well wrapped up.<br>
        <br>
        What can be done to permanently change individual habits that destroy 
        performance abilities? Goldsmith outlines Four Commitments that will 
        enhance your image with co-workers. Researched from two angles – at the 
        workplace, and at home – the exercise is for those that want to change. 
        Goldsmith urges you to convince yourself that you wish to change.<br>
        <br>
        The next six chapters are all about ‘six practices’ that will help you 
        be a better boss. It’s all very simple sounding. Yet, the ‘practices’ 
        could be trying for any person; more, for those that haven’t convinced 
        themselves about the need to change, the need to get out of the “I run 
        this place my way” syndrome.<br>
        <br>
        The book ends with the formula for implementation of the changeover 
        programme. There are eight rules that show how change can be theorised. 
        Again, it’s all introspective, but you get the ‘feel’ of what makes a 
        good boss.<br>
        <br>
        Goldsmith is not really attempting to convert people into angels of the 
        workplace. But he emphasises the importance of good leadership to 
        business.<br>
        <br>
        If more people realise that attitudes can be repaired, BOSS will no 
        longer be a ‘four-letter word’ in the workplace! And, from that change 
        will emerge the cornerstones of a good boss-subordinate relationship – 
        trust, loyalty, supportive communication and dependency.</font></p>
        <p align="right"><b><font face="Verdana" size="1">– Ganesh S.</font></b></p>
        <p><font face="Verdana" size="1"><b>What got you here won’t get you there<br>
        </b>Marshall Goldsmith<br>
        Hyperion, New York 2007</font></p></td>
