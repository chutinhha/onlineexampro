</font><b><font face="Verdana" size="4">Golf Highway</font></b><font face="Verdana" size="1"><br>
<b>The world’s longest golf course, at an unbelievable 750 miles long, is coming 
up in Australia</b></font><p align="center" style="margin: 3px 10px">
<font face="Verdana" size="1">
<br>
<img border="0" src="golf.jpg" width="400" height="290"></font><p align="justify" style="margin: 3px 10px">&nbsp;<p align="justify" style="margin: 3px 10px">
<font face="Verdana" size="1">Even those who don’t understand what club or tee means in golf parlance would be 
amazed if we tell them that a giant golf course is to be built in Australia. The 
Nullarbor Links course will spread over 750 miles, from tee-off in Kalgoorlie, 
Western Australia, to the 18th hole in Ceduna, South Australia. Players will go 
across the huge, arid Nullarbor Plain, and travel the full extent of the Eyre 
Highway, one of Australia’s classic routes, stopping off to play a hole in towns 
along the way. The course is expected to open towards the end of next year. The 
local authorities behind the project believe that it will increase tourist 
influx to the isolated spots. Each stop will have at least one other attraction, 
from fossil beds to whale-watching centres.<br>
<br>
<img border="0" src="golf1.jpg" width="200" height="184" align="right" hspace="6" vspace="4">The concept was developed by Bob Bongiorno while managing the Balladonia Road 
House on the Eyre Highway in 2000. He discussed the idea with a number of people 
who all thought the concept had merit. In August 2004, Bob spoke to Alf Caputo 
from the Eyre Highway Operators Association and Alf immediately recognised the 
enormous potential of the concept. The benefits could be unlimited for the 
participating roadhouses and towns. Alf discussed the idea with a number of 
politicians who indicated they were prepared to support the concept.<br>
<br>
<b>Nuts and bolts<br>
</b>The concept is basic. Each roadhouse would have one hole placed near their 
site. Towns that already have existing golf courses would be able to use one 
nominated hole. Each player would purchase a scorecard and ball container at 
either Kalgoorlie or Ceduna – either end of the Nullarbor Plain. The player 
would purchase a ball at each of the sites visited. On completion of the hole, 
the used ball would be placed on display in the container, as a memento of 
having played the course.<br>
<br>
Each roadhouse would stamp the player’s card on completion of the hole. On 
arrival at either Kalgoorlie or Ceduna – depending whether the player was 
travelling east or west – the player would have his card validated at either the 
course or a nominated business within the town.<br>
<br>
Plans are for the Nullarbor Links to become an international tourist icon. It 
will be the only golf course of its kind in the world. This 18-hole course will 
bring together the tourism aspirations of the Nullarbor community and will have 
a positive effect on increasing the number of tourists using the road link to 
the eastern states, while making their journey just that little bit more 
enjoyable and entertaining. A Pro-Am tournament would create enormous awareness 
in state, national and international golf clubs.<br>
