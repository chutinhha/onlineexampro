<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
</font><b><font face="Verdana" size="4">Boomtime for MSM</font></b><font size="1" face="Verdana"><br>
<b>MSM index has given incredible returns in 2007</b><br>
<br>
The Muscat Securities Market (MSM) witnessed strong performance during the 
current year. The benchmark MSM-30 general index generated a year to date (as on 
November 15) return of 46.1 per cent beating all other GCC bourses. In fact, in 
Oman, the local stock market has been going through a boom phase in view of 
windfall oil gains and the consequent growth in government spending. Revenues 
from the oil sector still contribute close to half of the country’s GDP, and the 
rise in the international prices of crude has had its positive impact in the 
form of higher current account surplus and excessive liquidity in the financial 
system. Accordingly, the government also has drawn up its expenditure plans, 
which involves hundreds of millions rials to be spent on infrastructure, tourism 
and oil and gas sectors. <br>
<br>
The nominal GDP of Oman grew at a rate of 15.6 per cent in 2006, slower than the 
rate of growth of 24.6 per cent exhibited in 2005 (from a lower base); and still 
this could be considered high in a relative sense, comparing with other emerging 
economies. Diversification has played a key role in the growth of the economy, 
as non-petroleum activities grew at a rate of 34.9 per cent, while petroleum 
activities declined to reach a growth rate of 14.8 per cent from a high of 43.9 
per cent in 2005. Non-oil exports climbed 46.3 per cent in 2006, while oil 
exports retracted to a mere 8.1 per cent from 45.2 per cent in 2005. </font>
<p align="justify" style="margin: 3px 10px">&nbsp;<p align="center" style="margin: 3px 10px">
