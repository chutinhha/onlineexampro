<font size="1" face="Verdana"><b>Performance of other GCC markets</b><br>
Apart from Oman, all GCC markets also ended in the positive territory for the 
year to date (YTD) period ending November 15. The Abu Dhabi market was the 
second largest YTD gainer, with the general index there gaining 40.19 per cent. 
The real estate index was the ace performer there, ending with a YTD gain of 
153.72 per cent followed by the banking and industrial indices, which generated 
YTD returns of 30.66 per cent and 23.11 per cent, respectively. The Dubai market 
was the next best performing market gaining 36.32 per cent YTD. Investment and 
the banking indices were among the top achievers on the Dubai exchange, closing 
86.75 per cent and 33.44 per cent, respectively. The Doha market generated a YTD 
return of 35.45 per cent on the back of strong buying specially in the 
industrial, services and banking counters, which increased by 82.97 per cent, 
29.98 per cent and 26.54 per cent, respectively. The Kuwaiti and Bahraini 
markets were up by 24.87 per cent and 18.81 per cent, respectively. While the 
banking index was the top performer on the Kuwaiti bourse, gaining 43.91 per 
cent, the investment index ruled the Bahraini bourse, rising by 22.44 per cent. 
The Saudi bourse, after staying in the red for most of the year, recovered in 
recent months, generating an YTD return of 16.26 per cent as on November 15. The 
insurance and industrial indices performed well and gained 57.95 per cent and 
47.76 per cent, respectively.<br>
<br>
It needs to be highlighted here that the Muscat Securities Market remained the 
‘top gainer’ over all other GCC markets, with a YTD return of 46.1 per cent. The 
factors that helped this market to be the best wealth creator in the region are 
the strong performance of the local corporates (on the back of the current 
economic boom), lower volatility in the market as compared to its GCC peers and 
better corporate governance practices, which leaves lesser room for bad 
surprises to large institutional investors.</font><p align="justify" style="margin: 3px 10px">
