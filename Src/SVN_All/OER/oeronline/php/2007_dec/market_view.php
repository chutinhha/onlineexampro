<font size="1" face="Verdana">
<br>
<b>Economic Expansion</b><br>
This economic expansion, never before witnessed in the history of the country, 
has been the driving force for local companies in expanding their toplines and 
bottomlines. In fact, for many industrial establishments listed in the local 
securities market, a booming GCC market in terms of rising construction 
activities and other capital investments has been a blessing.<br>
<br>
During the first nine months of the current fiscal, the banking, industrial and 
services sectors saw their aggregate earnings grow by 29.2 per cent, 26.5 per 
cent and 24.7 per cent, respectively. As against this, the three sectoral 
indices of the banking, industrial and services sectors gave a return of 51.6 
per cent, 44.8 per cent and 39.4 per cent, respectively. The rising ‘wealth 
effect’ and large leveraged investments in capital goods has been driving the 
banking sector profits. With large-scale asset build-up in their books, the 
banks in Oman are expected to report even higher growth in earnings in the 
ensuing quarters. Strong demand scenario within Oman as also in the region has 
given a major boost to the manufacturing sector, with most units being forced to 
add more capacities to meet the fast-growing demand. Again, with growing 
infrastructure and rising economic activity, the services sector too has been a 
major beneficiary, with many companies reporting high-double-digit growth in 
earnings.<br>
<br>
Two important events that marked the rise in market activity and led to the rise 
in general index by over 2500 points were the resurgence in the index 
heavyweight Omantel, and the primary float of one of the largest family-owned 
businesses in Oman, Galfar Engineering. During the third quarter, Omantel 
announced that the Board has approved the acquisition of a majority stake in 
Worldcall Ltd, a Pakistan-based telecom company. This was perceived by investors 
to be the right strategy for a company, which faces near-saturation in its 
operations within Oman. However, the news that brought back large institutional 
investors to the counter was the decision by the government to reduce the 
royalty being paid by Omantel and its competitor company for fixed and mobile 
phone services to 7 per cent instead of 10 per cent and 12 per cent, 
respectively. Among the other measures taken by the government was divestment of 
a portion of its stake in Omantel to a strategic investor. The stock appreciated 
almost 36 per cent on this news, while it weighed almost 20 per cent in the 
MSM-30 index! The breaching of 6000, 7000 and 8000 levels of the index were all 
witnessed in 2007, with the last one happening much sooner than expected.<br>
<br>
Galfar Engineering and Contracting Company’s initial public offering mobilised 
RO872.97 million, indicating an oversubscription of 14.5 times. Galfar made an 
all time record in IPO collection in the Sultanate, even bigger than the funds 
collected by Omantel. The institutional investors, who got 40 per cent of the 
IPO, offered RO790.72 million while individuals put in RO82.25 million. As 
expected, Galfar shares witnessed a strong secondary market, as institutions 
were unable to get all the shares they wanted. High net worth individual 
investors too showed strong interest, as they paid just RO0.602 for a share 
which returned over 82 per cent upside on the first day of listing. The listing 
of such large cap companies is bound to increase the depth of the market, which 
would be largely beneficial for large institutions.</font><p align="justify" style="margin: 3px 10px">&nbsp;<p align="center" style="margin: 3px 10px">
