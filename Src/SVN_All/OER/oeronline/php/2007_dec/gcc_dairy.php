		</font><b><font size="4" face="Verdana">Future Brand:<br>
        UAE on top of country brand index</font></b><font face="Verdana" size="1"><br>
        <br>
        <b>The UAE has been rated as one of the ‘rising stars’ among the world’s 
        top tourist destinations by an independent global study, after winning 
        the coveted Best Country Brand in the Resort/Lodging Options category 
        for the first time</b></font></p>
        <p align="center" dir="ltr"><font face="Verdana" size="1">
        <img border="0" src="gcc1.jpg" hspace="6" vspace="6"><br>
        <b>Multi-billion dollar tourism projects coming up in UAE</b></font></p>
        <p align="justify"><font face="Verdana" size="1">According to the 2007 Country Brand Index (CBI), the UAE is one of the 
        world’s top two shopping havens, and is among the top three countries 
        with the highest potential to become a major tourist destination in the 
        next five years.<br>
        <br>
        Country Brand Index, now in its third year, is a comprehensive study of 
        more than 2,600 international travellers, conducted by FutureBrand, a 
        leading global brand consultancy, in conjunction with public relations 
        firm Weber Shandwick’s Global Travel &amp; Lifestyle Practice. The Index 
        examines how countries are branded and ranked according to key criteria, 
        with this year’s index also including trends on travel motivations as 
        well as challenges and opportunities within the divisions of travel, 
        tourism and country branding.<br>
        <br>
        “A strong country brand provides an opportunity for a country to 
        showcase its values across every government sector,” said Rina Plapler, 
        Executive Director, FutureBrand. “In this year’s Country Brand Index, we 
        describe how multifaceted countries act as brands and how they require 
        specific frameworks, typologies and the use of considered semiotics.”<br>
        <br>
        The UAE won the Best Country Brand for the ‘Resort/Lodgings Options 
        category, thus being recognised for its unrivalled reputation as the 
        world’s leading resort and lodging destination. Mega projects such as 
        the Palms and other distinct resort developments propelled the country 
        to the highest position on the scale, narrowly beating the Maldives and 
        Australia.<br>
        <br>
        The UAE was also placed second in the Best Country Brand in the Shopping 
        category, just behind the US, but ahead of countries like Singapore, 
        Italy and Japan. The UAE’s huge investments to build up its MICE 
        (Meetings, Incentives, Conferences and Events) facilities have also paid 
        off, with the country coming in the top five in the Best Country Brand 
        for Conferences category.<br>
        <br>
        The UAE was also recognised for its trend-setting initiatives in travel 
        and tourism, which aim to extend brand experience in everyday life. For 
        instance, world-renowned fashion labels such as Armani and Versace were 
        the first to infuse their signature aesthetics among hotels in Dubai.<br>
        <br>
        “A country’s brand reputation is in itself a powerful currency. 
        Reputation drives financial and business investment, sustainable growth 
        and helps add fuel to the private and retail sectors,” said René A Mack, 
        President, Weber Shandwick’s Travel &amp; Lifestyle Global Practice.<br>
        “A visitor is the most powerful marketer for any country brand. They 
        import the destination and its products and experiences directly into 
        their homes, offices and social communities, combined with the most 
        powerful voice of all – word of mouth.”<br>
        <br>
        The UAE is the only GCC state cited in the Country Brand Index and one 
        of only two Middle East countries that have figured prominently on the 
        Index. Egypt also made its mark by topping the Best Country Brand for 
        History, and joining Italy and France among the top three in the Best 
        Country Brand for Art and Culture. Australia was once again the world’s 
        top country brand, according to the Country Brand Index, with the US 
        second and the UK ascending to the third place. Other countries making 
        the list included Japan, Canada, France and Greece. Aside from the UAE, 
        the Country Brand Index also identified Croatia, China, Cuba, Iceland 
        and the Russian Federation as potential major tourist destinations in 
        the next five years.<br>
