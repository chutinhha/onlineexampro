        </font><font face="Verdana" size="2">
        <b>GulfMerger: Continuous growth in consumer finance</b></font><font face="Verdana" size="1"><br>
        Industry research from GulfMerger, an independent financial advisory 
        firm, forecasts continued strong growth in Kuwait’s consumer finance 
        market over the next five years.<br>
        <br>
        The research also reveals that Kuwait’s consumer finance companies are 
        poised to go through a wave of local consolidation and regionalisation, 
        driven by industry players and private equity houses with clear 
        strategic and financial imperatives. The Kuwaiti consumer finance market 
        has grown rapidly in recent years, with consumer loan portfolios growing 
        from KD3.9 billion in 2005 to KD4.6 billion in 2006. According to 
        GulfMerger, car financing has been a key contributor, accounting for 
        KD340 million in new loans in 2006, and an estimated KD390 million in 
        new loans in 2007. Based on current demographic and macroeconomic 
        trends, GulfMerger expects car financing in Kuwait to grow from an 
        annual financing of 65,000 cars in 2007 to over 90,000 cars by 2012, 
        resulting in KD670 million in new car financing loans in 2012. These 
        forecasts are based on the combined effect of three factors: increasing 
        population growth, higher car ownership, and an increasing propensity by 
        car buyers to finance their purchases. Taking each factor in turn, 
        GulfMerger expects Kuwait’s population to grow from 3.4 million to 
        approximately 4.4 million inhabitants over the five year period ending 
        2012, car ownership to expand from 315 to 340 cars per 1,000 
        inhabitants, and car financing to increase from 65 per cent to 
        approximately 70 per cent of all new cars purchased in light of the 
        greater availability of finance providers.<br>
