        <font face="Verdana" size="2"><b>Bahrain to offer ‘clean’ tourism</b></font><font face="Verdana" size="1"><br>
        <img border="0" src="gcc10.jpg" width="300" height="200" align="right" hspace="6" vspace="4">Bahrain’s Information Minister Dr Mohammed bin Abdulghaffar has said 
        that his ministry was currently studying 35 proposals from Bahraini and 
        foreign investors to set up four- and five- star hotels which should 
        give a big boost to plans for a ‘clean tourism’ in the Kingdom. In an 
        interview with the London-based Arabic daily Asharq Al Awsat, the 
        Bahraini minister said that the era of substandard tourism was over in 
        the Kingdom, where night clubs had operated for years without any 
        supervision.<br>
        <br>
        According to him, the new strategy to offer clean tourism was meant to 
        transform Bahrain into a place for family tourism. The goal was to 
        reorganise and diversify facilities to create a high standard of tourism 
        environment for residents and visitors.<br>
        <br>
        In this context, Dr Abdulghaffar said the Kingdom organises a spring 
        festival every year showcasing the diversity of cultures and stages 
        events of international standards. The minister added that the decision 
        to reorganise tourism in the Kingdom should not drive away investors, 
        adding that the Kingdom would not compromise on values just for the sake 
        of attracting investment. Asked whether these decisions were being 
        guided by religious groups, Dr Abdulghaffar said the aim was to develop 
        clean tourism. He also stressed that nightclubs at four- and five-star 
        hotels offered high-quality shows without any violation of guidelines.
        <br>
        <br>
        In this regard, he said the government would not renew night clubs 
        licences in one- and two-star hotels, about 65 in all, since they 
        depended entirely on their clubs and brought a “bad reputation” to the 
        Bahraini tourism sector. He said that disputes on this issue were the 
        result of differences among various interested factions but the 
        government policy of cleaning the tourism sector was final.<br>
