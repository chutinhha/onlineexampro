        <font face="Verdana" size="2"><b>Kuwait to beat Burj</b></font><font face="Verdana" size="1"><br>
        <img border="0" src="gcc6.jpg" align="right" hspace="6" vspace="4">Now it is the turn of Kuwait to build the tallest building in the world 
        after Dubai. The local authorities have approved initial plans for a 
        colossal new development that includes a tower expected to be over one 
        kilometre high. Kuwait’s Municipal Council has also approved sketches of 
        the 250 sq km development, known as the Madinat Al-Hareer, which will be 
        built opposite the capital city across Kuwait Bay.<br>
        <br>
        The Madinat Al-Hareer, or City of Silk, is expected to take around 25 
        years to build and cost an estimated US$86 billion. There is no 
        indication yet as to when construction will begin. The project includes 
        construction of a 200-hectare natural desert reservation, a new airport, 
        a large business centre as well as areas that concentrate on 
        environment, sports, media, health, education, and industry. At its 
        centre will be the Burj Mubarak Al-Kabir, a tower soaring 1,001 metres 
        into the air – almost twice the height of Taiwan’s Taipei 101, which is 
        509 metres tall and officially still the world’s tallest building.<br>
        <br>
        The Burj Dubai overtook Taipei 101 earlier this year as the world’s 
        tallest building, but will not be officially recognised until 
        construction is completed at the end of next year. <br>
