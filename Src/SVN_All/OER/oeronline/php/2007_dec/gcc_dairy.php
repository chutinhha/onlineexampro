        </font><b><font face="Verdana" size="2">Moody’s:</font></b><font face="Verdana" size="1"><br>
        <b>Why wealthy Gulf states are not rated Aaa</b><br>
        The six-member states of the Gulf Cooperation Council – Bahrain, Kuwait, 
        Oman, Qatar, Saudi Arabia, the UAE – have enjoyed an unprecedented 
        economic boom over the past five years. Reflecting their enhanced 
        creditworthiness and ability to cope with potential shocks, they have 
        received sovereign ratings upgrades from Moody’s Investors Service of 
        between two and four notches since 2003.<br>
        <br>
        Such has been the degree of their economic improvement that some GCC 
        countries now outperform even Aaa-rated countries, according to several 
        of Moody’s comparative ratios. Yet even the wealthiest Gulf states 
        (Kuwait, Qatar and the UAE) do not currently have ratings higher than 
        Aa2, Moody’s says in a new Special Comment. In essence, this is for 
        three key reasons: regional geopolitical vulnerabilities, the developing 
        state of domestic institutions, and the volatility of economic 
        performance associated with a heavy concentration on hydrocarbons.<br>
        <br>
        “Measured in output per capita, the prosperity of the six GCC countries 
        has climbed sharply in recent years, in some cases to very high levels, 
        on the back of soaring global oil prices and impressive progress in 
        economic diversification. Perhaps, most importantly from a sovereign 
        ratings perspective, there has been a dramatic strengthening of 
        governments’ balance sheets over this period as fiscal and current 
        account surpluses have widened. This is despite a strong pickup in 
        government expenditure and imports,” says Tristan Cooper, Moody’s 
        Vice-President / Senior Analyst and author of the report.<br>
        <br>
        “The considerable enhancement to governments’ creditworthiness has been 
        well reflected in Moody’s sovereign ratings, which now range from A2 for 
        Bahrain and Oman, to A1 for Saudi Arabia, and Aa2 for Kuwait, Qatar and 
        the UAE. These are high investment grade ratings and denote a very low 
        risk of default,” Cooper notes. “However, even though some GCC states 
        now outperform even Aaa-rated countries on some economic indicators, 
        even the wealthiest among them are not rated Aaa. This is for several 
        reasons. Firstly, Aaa-rated countries tend to be located in regions with 
        a long track record of political stability, whereas the Middle East – 
        and particularly the Gulf region – has a more troubled political 
        history,” Cooper explains. <br>
        <br>
        “Secondly, institutions in the GCC continue to be of a more developing 
        nature than those in Aaa-rated countries. The evaluation of 
        institutional strength encapsulates the quality of governance, including 
        the effectiveness of countries’ administrative, legislative and judicial 
        apparatus. Such factors are important for determining sovereign ratings 
        because they are an indication of the likely stability and consistency 
        of the domestic policy environment over the longer term. GCC states also 
        face some challenges in regard to the quality and scope of economic 
        data, which hinders effective monitoring,” Cooper adds.<br>
        <br>
        “Finally, GCC countries’ economic performance has exhibited marked 
        fluctuations, far more pronounced than those of Aaa-rated countries, in 
        light of the still heavy concentration of their economies on oil and gas 
        despite material progress towards diversification. This trait is 
        pertinent to the assessment of sovereign risk because, globally, 
        countries displaying higher levels of economic volatility have tended to 
        be more prone to shocks and therefore to have a greater risk of default. 
        However, Moody’s of course notes the stabilising role of growing 
        sovereign wealth funds in the region.”</font></p>
