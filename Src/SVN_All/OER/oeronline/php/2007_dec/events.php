<p align="justify" dir="ltr" style="margin-left: 5; margin-right: 5"><b><br>
</b><b><font face="Verdana" size="4">Oman Web Awards 2007<br>
</font></b><font face="Verdana" size="1"><br>
</font><font face="Verdana" size="2">
<b>Open for Registration</b></font><font face="Verdana" size="1"><br>
<br>
<img border="0" src="events.jpg" width="250" height="181" align="right" hspace="6" vspace="4">It’s time to revamp those sites again, as the much awaited Web Awards is 
launched for 2007 under the auspices of Information Technology Authority (ITA). 
Instituted in 2005 in association with Pan Arab Web Awards, Oman Web Awards (OWA) 
strives to recognize excellence in web design and to promote skill development 
among the local community. In its glorious third year, the Oman Web Awards is 
presented by Advanced Business Solutions, supported by Business Software 
Alliance and hosted by SJS Group in Oman. The official Magazine is Oman Economic 
Review and Alam Al Iktisaad Al Wamal and Times of Oman &amp; Al Shabiba are the 
official News papers, Ruwi Modern Printers is the official printer and Oman Air 
is the official Airlines. The Category sponsor in HSE is Oman WasteWater 
Services Company SAOG.<br>
<br>
Being the only web award at national level, the main objective of OWA is to 
promote the spirit of innovation and creativity, raise the standards of web 
based solutions, advocate development of local talent, promote intellectual 
property awareness and encourage all sectors to become involved in the fast 
growing digital economy of Oman.<br>
<br>
Dr. Salim Al Ruzaiqi, CEO of ITA opined on the award as follows: “ITA, being the 
apex government body for IT in the Sultanate, we are committed to support the 
ICT oriented events in Oman. We believe that the event spurs activity in the 
digital word which in turn strengthens the eOman initiatives of ITA in 
transforming Oman as knowledge based economy by offering improved services to 
the society at various levels. Therefore we consider that OWA can generate the 
spirit of excellence in the citizens of Oman to work for a better society 
leveraging IT and we would encourage all entities to participate in this event 
and wish them all the success for winning it.”<br>
<br>
Participating in OWA helps to validate the customer’s increased confidence in 
the organization and its services. The winners will qualify for the Pan Arab Web 
Awards 2008, where web award winners of Bahrain, Egypt, Jordan, Kuwait, Lebanon, 
Oman, Qatar, Saudi Arabia and UAE will compete. <br>
<br>
Apart from the awards for the Best Website and Web Design Company of the year, 
there are 24 categories with winners in Gold, Silver &amp; Bronze respectively. The 
categories in the competition are, Art &amp; Culture, Automotive, Banking &amp; Finance, 
Corporate, Education, Forums, Hospitality, FMCG, Health Services, 
Malls/Supermarkets, Real Estate, Software &amp; Telecommunication/ICT, NGO/NPO, 
E-commerce, Travel &amp; Tourism, Oil &amp; Gas, Media/Media Services &amp; Event 
Management, Ministries/Government Organizations, Engineering/Manufacturing 
Industry, Sports, Services, Retail, Health, Environment &amp; Safety (HSE) and 
Personal websites. Sponsorship is open in all the above categories. <br>
<br>
In the words of Ali Jaffer Al Bahrani, Chairman of Oman Web Awards and Managing 
Director of ABS, “Awarding the design and development of web sites, the primary 
mission of ABS is to encourage individuals, organizations and businesses to make 
the best use of the Internet and World Wide Web technology; at the same time 
adding character, mood and a theme based upon the content of the particular web 
site requirement.” <br>
<br>
The newly constituted Web Designer of the Year Award is to recognize the web 
designing companies for their efforts. The grand award ceremony is scheduled to 
take place early 2008 in Muscat.<br>
<br>
