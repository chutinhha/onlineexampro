</font><b><font face="Verdana" size="4">
BUILDING CAPACITY</font></b><font face="Verdana" size="1"><br>
<img border="0" src="cover13.jpg" width="175" height="480" align="left" hspace="6" vspace="4">In ancient days, Oman, then known as Majan, was at the forefront of 
international maritime trade as Omani dates, traditional handicraft and 
fragrances were very popular in Africa, Middle East, Europe and Asia. With the 
discovery of hydrocarbon reserves in the 20th century, like rest of the other 
Gulf states, Oman’s economy shifted focus to the oil and gas sector. However, 
with the increased stress on diversification of the economy, Omani products 
across different categories are again coming to the fore and gaining wider 
acceptability in the international market.<br>
<br>
The strong growth recorded by the manufacture of non-oil products has been one 
of the most impressive aspects of the country’s macroeconomic performance in 
2007. According to gross domestic product (GDP) figures up to June 2007, all 
industries in Oman have grown significantly. The most impressive has been the 
refined petroleum products sector that has grown by 767 per cent over the 
corresponding period in 2006. The mining and quarrying sector has gone up by 
51.7 per cent and construction by 21.6 per cent.<br>
<br>
On the steps taken during the year to boost industries in Oman, HE Maqbool bin 
Ali Sultan, Minister of Commerce &amp; Industry, said: “The ministry has done a 
survey of over 168 installations to have a clear idea on their real conditions 
and we found that 46 per cent of them were doing well whereas 32 per cent are 
currently in trouble, in addition to 18 per cent that may face trouble in the 
future. The poor performance of these companies may be attributed to poor 
management system, lagging in taking investment decision, poor marketing 
initiatives and inability to develop their products to meet the tough 
competition at the market. We have taken a number of measures to help the 
stumbling companies introduce some corrective measure. We also organised a 
number of seminars and workshops to raise the awareness of investors.”<br>
<br>
The growth of exports of non-oil/gas products of Omani origin has been 
outstanding this year. The value of non-oil/gas Omani origin exports touched 
RO469.2 million ending May 2007 as against RO258.1 million during the same 
period in 2006, registering an impressive growth rate of 82 per cent. The best 
performing product categories include mineral products (406.8 per cent growth), 
electrical machinery, mechanical equipment and parts (155.3 per cent growth), 
base metal and articles (15 per cent growth), articles of stone, plaster, cement 
and ceramic products and glassware (14 per cent growth) and live animals and 
animal products (13.5 per cent growth).<br>
<br>
<img border="0" src="cover14.jpg" width="250" height="166" align="right" hspace="6" vspace="4">Oman is set to cross the RO1-billion-mark in non-oil/gas exports in 2007 riding 
on the growing popularity of ‘Made in Oman’ products. The Omani Centre for 
Investment Promotion and Export Development (OCIPED) has set out an export 
target of RO2 billion for non-oil/gas Omani origin products by 2010.<br>
<br>
To promote industries in Oman, the government has made strategic investments in 
the companies and is building up the infrastructure conducive to the growth of 
industrial ventures. This has attracted foreign investors and private sector 
players to increase their ante in Omani industries. Large industrial ventures, 
including Oman India Fertiliser, Sohar Refinery, Oman Polypropylene and Oman 
Methanol, have initiated commercial production. The $969 million-Oman India 
Fertiliser project has already started generating significant revenues by 
exporting products to India.<br>
<br>
The US$1.2 billion-Sohar Refinery, which started operation last year, is 
ensuring production of refined petroleum products and providing feedstock for 
many petrochemical ventures in Sohar. Its major products include propylene, LPG, 
straight run naphtha, regular unleaded gasoline, premium unleaded gasoline, 
aviation fuel, diesel, fuel oil and sulphur.<br>
<br>
The refinery has led to a new era of growth in the downstream oil activities in 
Oman. Its optimum production of 327,000 tonnes per year serves as feedstock for 
the polypropylene project of Oman Polypropylene. Oman Petrochemical Industries 
Company’s (OPIC) world-class plant will produce 1 million tonnes of high quality 
polyethylene per annum to generate an annual turnover of US$1 billion. The OPIC 
project is a 50:50 joint venture of Oman government and Dow Chemicals.<br>
<br>
The petrochemical complex, located in the Sohar Industrial Port Area, comprises 
feedstock production facilities, a gas cracker, as well as three world-scale 
polyethylene production units based on state-of-the-art catalyst and process 
technology. The joint venture is expected to facilitate the development of 
downstream industries in Oman that will convert polyethylene to end-products. 
The first petrochemical project that went on stream in Sohar was a polypropylene 
project – a significant industry that enhances non-oil exports. The Oman 
Polypropylene, jointly promoted by Oman Oil Company, Bahrain based Gulf 
Investment Corporation and LG International, was set up with a capital 
expenditure of $313 million. The project has an installed capacity of 340,000 
tonnes per annum of polypropylene in pellets form, for a variety of 
applications. OPP has an off take agreement with LG International for selling 
its products. Most of the output of OPP is for the overseas market, with the 
remaining quantity used locally by ancillary industries. Polypropylene is used 
to produce several items such as food packages, flower wrapping, furniture, 
bottles, just to name a few.<br>
<br>
Another petrochemical venture that started shipment is Oman Methanol Company 
(OMC), which has a capacity to produce 3,000 tonnes per day. OMC is jointly 
owned by Methanol Holdings (Trinidad) Limited, Omar Zawawi Establishment, and 
MAN Ferrostaal AG. The company has an off take agreement with HELM AG. Apart 
from becoming increasingly important as a very flexible energy source and as a 
co-component for bio-diesel, methanol is a key chemical intermediate for several 
industrial chemicals, including polyesters, acrylics and formaldehyde resins.<br>
<br>
Sohar International Urea and Chemical Industries, another gas-based venture 
promoted by private entrepreneurs, is set to commission its plant in January. 
With an estimated capital expenditure of $638 million, the project will use 
natural gas as feedstock to produce 2,000 tonnes of ammonia and 3,500 tonnes of 
granular urea per day. Granular urea will be exported globally under a long term 
off take agreement already entered into with the US-based Transammonia Inc.<br>
<br>
Sohar Aluminium Company (SAC) is considered as one of the largest industrial 
ventures that will change the face of Sohar. With an investment of roughly $2.3 
billion, it is considered as the single biggest industrial undertaking in the 
Sultanate. SAC is backed by Oman Oil Company (OOC) and the Abu Dhabi Water and 
Electricity Authority (ADWEA), which have a 40 per cent stake each, with 
Canada-based Alcan Inc holding the remaining 20 per cent. The 325,000 tonne per 
annum aluminium smelter is coming up at a sprawling site adjacent to the 
existing Sohar Industrial Area.<br>
<br>
Sohar Aluminium is building a single AP35 potline, together with associated 
carbon and casting facilities and a 1000MW-gas fired power station. The company 
will be the first smelter in the world to pioneer use of “best-in-class” 
Pechiney AP35 technology. Alcan will provide the smelting technology and 
technical assistance, besides serving as the metal offtaker. The project, which 
is expected to contribute 2 per cent to the country’s gross domestic product 
(GDP), will have significant socio-economic benefits for the Sultanate. Roughly 
35 per cent of the estimated 1,000 direct jobs envisaged at start-up will be set 
aside for Omanis – a figure set to rise to 60 per cent of the workforce after 
five years. In addition, some 4,000 indirect jobs will be created as a result.<br>
<br>
Another significant venture is Shadeed Iron &amp; Steel, which is building an 
integrated steel complex at the Sohar Industrial Port. This is a project that 
has the potential to catapult the Sultanate into the select ranks of 
steel-producing nations. Backed by UAE-based business house Al Ghaith Holding, 
the company will initially invest around $350 million in the construction and 
management of a world-class steel plant. Investments could total as much as $750 
million when subsequent phases are implemented, leading to the creation a giant 
facility rivalling in size plants already in operation in Saudi Arabia and 
Qatar. In the first phase, Shadeed Iron &amp; Steel is building a plant designed to 
produce around 720,000 tonnes of hot briquetted iron (HBI) including 500,000 
tonnes of steel billets.</font></p>
