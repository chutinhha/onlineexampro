<b>Tourism</b></font><font face="Verdana" size="1"><br>
<br>
</font>
<b><font face="Verdana" size="4">
STRIDING AHEAD</font><font face="Verdana" size="1"><br>
</font>
</b><font face="Verdana" size="1">
<img border="0" src="cover6.jpg" align="left" hspace="6" vspace="4" width="175" height="583">From the tourism perspective, a number of major developments have taken 
place in 2007 that have sown the seeds of major transformation in the medium to 
long term. In the hotel and resorts segment, at least two major hotels– Al 
Bustan Palace and Sheraton –remained closed for almost the entire year. This has 
definitely put pressure on the other properties such as ShangriLa Barr Al Jissah, 
Intercontinental and Grand Hyatt. But they are not complaining, as business 
boomed for them throughout the year, dipping only a little as an after effect of 
Cyclone Gonu. <br>
<br>
As per the half-yearly results from the HotelBenchmark survey by Deloitte Touche 
Tohmatsu (Deloitte), Muscat saw the strongest growth in the Middle East, with 
revPAR up by 54.8 per cent to US$164. The report stated: “The growth in Muscat 
has been driven predominately by an impressive 44.2 per cent increase in average 
room rates (pegged at US$216). Hoteliers continue to benefit from the closure of 
four key hotels for extensive refurbishment due to scheduled upgrades. Also, the 
June cyclone caused significant infrastructure damage and forced many residents 
to shift into temporary hotel accommodation.”<br>
<br>
On the expansion front, a number of hotel projects, with investments running 
into billions of dollars, were announced during the year. Among the more 
prominent ones, Muriya Tourism Development Company (MTDC) announced in November 
two prestigious projects, ‘Jebel Sifah’ in the capital governorate and ‘Salalah 
Beach’ in the southern Dhofar region. The twin integrated tourism complexes (ITCs) 
will comprise of a total of 550 exclusive villas and 1,200 apartments when the 
first phase is completed in five years. Besides villas and apartments, the 
projects will boast more than four hotels each, world-class marinas and marina 
towns, 18-hole PGA golf courses, restaurants, and international retail outlets.<br>
<br>
The US$2 billion beachfront tourism and residential project, The Wave Muscat, 
has tied up with two major hotel groups, Fairmont and Kempinski, for its first 
two hotels of roughly 300 rooms each. According to Nick Smith, CEO of The Wave, 
Muscat, “the actual construction of the hotels will start in 2008. Both the 
hotels should open in 2010.”<br>
<br>
Much headway has been made in the Blue City project, one of the largest 
developments in the Middle East, with an estimated investment between US$15-20 
billion. The first round of financing was secured through the issuing of a bond 
note by Bear Stearns, raising US$925 million. The note was rated Baa3 by ratings 
agency Moody’s in March 2007, following on from a BBB- rating by Fitch when the 
note was issued. The promoters, Al Sawadi Investment and Tourism (ASIT), have 
appointed Foster + Partners to develop the master plan for the Blue City 
project. Foster + Partners are one of the foremost architectural firms in the 
world and designers of iconic structures such as Millau Viaduct in France, Swiss 
Re Headquarters in London, Hong Kong’s Chek Lap Kok airport and the Hearst Tower 
in New York. <br>
<br>
<img border="0" src="cover5.jpg" width="250" height="167" align="right" hspace="6" vspace="4">AlArgan Towell Investment, part of WJ Towell Group, is also planning two major 
developments – a water and beachfront project near Muscat and a standalone 
luxury resort at Al-Kayiran – with a combined investment of RO500 million. At 
least three hotels are expected to come up at the beachfront property. The 
company is expected to start construction in the first quarter of 2008. 
“Al-Kayiran Resort is an exclusive, environmentally friendly resort in a 
heavenly crystal blue secluded natural bay, with a spectacular design. The world 
class luxury hotel resort offering no more than 82 keys brings to Oman one of 
the most prominent luxury hotel operator in the world,” a company spokesperson 
pointed out. <br>
<br>
In other developments, MB Holding Company’s chairman HE Mohammed Al-Barwani, in 
his personal capacity, is also busy working on a project to build a business 
park at Azaiba at an estimated cost of US$300 million. Construction is expected 
to start in 2009 and completed in 2011. “The business park will have high 
quality modern corporate offices, upmarket retail facilities, a five-star luxury 
corporate hotel and service apartments. We have already negotiated with a 
leading hotel chain and are close to signing a management contract appointing 
them as operators. That company will be entering Oman for the first time,” he 
informed. He is taking the advice of leading global consultants, such as Atkins, 
Cluttons and Dubai-based TRI and others, to give shape to his dream project.<br>
<br>
The aviation sector too witnessed dramatic developments in 2007. After months of 
speculation and denials by the Government of its withdrawal from Gulf Air, the 
Sultanate of Oman finally sold its stake in the airline to Bahrain in May. The 
strategy was well thought out. According to Ziad Karim Al Haremi, CEO, Oman Air, 
plans were afoot to sever ties with Gulf Air, but Oman Air and its management 
were unaware of the timing, catching every one by surprise.<br>
<br>
<img border="0" src="cover3.jpg" width="250" height="188" align="left" hspace="6" vspace="4">Looking back at the developments, one notices a pattern emerging. Soon after the 
divestment from Gulf Air, the Government decided to take full control of its 
flag carrier, Oman Air. The Government now owns 81 per cent stake having pumped 
in an additional RO37 million and its shares have been de-listed from the Muscat 
Securities Market. This leaves the Government free to pursue its plans to turn 
the Sultanate into a tourist destination and, to use a cliché, allow Oman Air to 
spread its wings to new destinations. The thrust now is on long- haul routes. 
The first stop on the planned network expansion is London in November, followed 
by Bangkok and, in 2008, other high tourist-yielding destinations in Western 
Europe like Frankfurt and Milan.<br>
<br>
Oman Air is acquiring six Boeing B787 Dreamliner aircraft from ALAFCO Aviation 
Lease and Finance Company headquartered in Kuwait. Oman Air will be the first 
airline in the Gulf region to obtain the Dreamliner aircraft, which are expected 
to be delivered in 2012, 2014, and 2015. Oman Air said the 787 would upgrade its 
existing fleet as well as provide for fleet growth to meet its future aggressive 
expansion plans.<br>
<br>
As per the data up to September 2007 on air traffic movement at the Seeb 
International Airport, the number of international flights (landing and 
take-off) went down by 0.5 per cent over the corresponding period in 2006. The 
number of passengers, in international flights, decreased by 9 per cent (source: 
Ministry of National Economy).</font></p>
