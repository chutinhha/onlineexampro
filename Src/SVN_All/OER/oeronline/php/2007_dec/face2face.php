<p align="justify" dir="ltr" style="margin-left: 5; margin-right: 5"><b><br>
<font face="Verdana" size="4">Can he do it?</font><font face="Verdana" size="1"><br>
Chiwon Suh, President – Middle East &amp; Africa (MEA), Samsung Electronics want to 
reach sales revenue of US$10 billion by 2011 in MEA market. Akshay Bhatnagar 
caught up with him on his flying visit to Muscat to find out what makes him 
oozing with such confidence<br>
<br>
<img border="0" src="cover17.jpg" width="225" height="245" align="left" hspace="6" vspace="4">What brings you to Oman?<br>
</font>
</b><font face="Verdana" size="1">With a population of over 2.5 million and per 
capita income of about US$14,000, Oman is an important market for Samsung. We 
enjoy a good market share in Oman especially in segments such as LCD TV, 
refrigerators and home appliances. I’m here to see how the market is shaping up. 
My last visit was in August last year. This is my third visit to Oman.<b><br>
<br>
What changes you have noticed in Oman in the last one year?<br>
</b>The market is transforming like any other market in the GCC region. The 
consumers increasingly prefer higher end products. That’s good news for us as we 
can feed such demand for premium products. The mobile phone business is growing 
as the demand has gone up dramatically. So Oman offers more business 
opportunities for us.<b><br>
<br>
What has been the growth rate of Samsung in Oman this year?<br>
</b>We have grown by almost 100 per cent this year over 2006 figures. I’m 
expecting another 100 per cent growth for Samsung in 2008.<b><br>
<br>
Do you think it is a realistic target?<br>
</b>Yes, I’m confident that we can grow by 100 per cent. I’ve checked the market 
and believe that it is an achievable target.<b><br>
<br>
Are you looking at roping in additional business partners in Oman?<br>
</b>We are happy with our current partners. We are delighted with the 
performance of the new management of SARCO, our consumer electronics distributor 
in Oman. <b><br>
<br>
What have been the achievements of Samsung in 2007 in ME and Africa (MEA) 
market?<br>
</b>We have performed incredibly this year. Our sales turnover in 2006 was 
US$2.5 billion. This year we are expecting it to surpass US$3.6 billion, more 
than 45 per cent annual growth.<b><br>
<br>
Your bosses in Korean headquarter are happy with the performance or they expect 
more from you?<br>
</b>Yes, they are happy and definitely expect a significant growth next year 
too. So far Samsung had focused on the more developed markets like US and Europe 
to sell its high end products. This year, the management changed the strategy 
and Samsung is focusing on emerging markets like Middle East and Africa. Due to 
this strategic shift, we are offering affordable products for the lower end of 
the market for higher market penetration.<b><br>
<br>
What is the break-up of your revenues from different product segments?<br>
</b>Difficult to say as the revenue share differs from product to product. 
Broadly speaking, the premium products contribute about 40 per cent to our top 
line. The middle segment and the affordable (low end) segment add 30 per cent 
each.<br>
<br>
The market character is changing and accordingly our product portfolio is also 
changing. We are gaining more revenue from the high end products. For example, 
if we see the TV business, there are three categories of products – CRT TV, 
Plasma TV and LCD TV. We call plasma and LCD as flat panel TVs. In quantity, we 
are expecting TV sales of about 2-million units in 2007. Around 30 per cent 
comes from the flat panel TVs and 70 per cent is contributed by the CRT TVs. In 
value terms, 30 per cent of flat panel TV sales give more than 75 per cent of 
our revenue in the TV business.<br>
<b><br>
<img border="0" src="cover18.jpg" align="right" hspace="6" vspace="4">Which countries are giving you the maximum business in the MEA region?<br>
</b>In terms of percentage growth, I can say our growth in Oman has been 
outstanding. With a growth rate of 100 per cent, the Omani market has 
outperformed Samsung’s growth of 45 per cent in MEA by a wide margin. That means 
we have more business opportunity in Oman.<br>
<br>
If we look at numbers, the biggest market for Samsung in MEA is Turkey. We have 
grown rapidly in Turkey in the last three years. Iran and South Africa are the 
other two large markets for us. Among the emerging markets in Africa, Nigeria is 
on our radar. We are keenly observing the market and planning to be more 
aggressive there. Nigeria has an immense potential in the mobile phone business; 
in terms of market size it is bigger than South Africa.<b><br>
<br>
You mentioned Iran as your major market. How do you look at that market in the 
context of the growing tension between Iran and US?<br>
</b>There have been problems between Iran and other nations in the past also. 
Yes, it has definitely affected our business from time to time but if we take a 
long-term view of the past, our business has grown considerably. Any escalation 
of the current tension could affect our business in the short term but we are 
bullish on Iran in the long term.<b><br>
<br>
Are you operating in Iraq as well as it is a fairly large market?<br>
</b>No, but we are working with our partners in Iraq very closely.<b><br>
<br>
How Iraq is developing as a market for Samsung in the recent time?<br>
</b>It is a pity that I cannot visit Iraq due to security issues. I have not 
been to Iraq yet. We have a good market for consumer electronics and mobiles 
there, and our partners are working hard there. Unfortunately, the market is not 
growing due to the turmoil as people are migrating from there and as a result 
the demand is not going up. Our business has not grown there this year.<b><br>
<br>
What is the share of your MEA operations in Samsung’s global revenues?<br>
</b>Samsung Electronics’ global revenue is around US$100 billion. We should be 
having around 3.6 per cent share in that. But if you look into the consumer 
electronics and mobile phones segment alone, it could be around six per cent.<b><br>
<br>
What is your vision for Samsung in MEA in the coming years?<br>
</b>Our target is to cross US$5 billion next year by registering a 40 per cent 
growth rate. We are working towards it. In terms of our long-term vision, we 
envision Samsung as the most preferred and loved brand in the region. We are 
looking at achieving US$10 billion sales by 2011. We intend to meet our target 
with a three-pronged approach – by ensuring brand leadership, product leadership 
and customer leadership. Our strategy is to invest more in brand communication 
campaigns, enhanced shop displays, market driven line-up, introducing customer 
oriented models and improving distributor relationship, public relations and 
corporate social responsibility activities. <br>
<b><br>
Are you looking at setting up a plant in MEA?<br>
</b>Not yet. We don’t have any such plans. <b><br>
<br>
Many Japanese companies are losing out to Korean companies in their traditional 
strongholds. What are the lessons that Japanese companies need to learn from the 
Korean companies like Samsung to bounce back?<br>
</b>Samsung has become the market leader in the 21st century and in the process 
has surpassed Japanese companies in terms of market share and revenue. The 
success of Samsung could be attributed to its dynamic management style. We are 
quick in decision-making. It is our biggest strength. We have a very fast supply 
chain management system. That’s the key of our success. Now I heard that 
Japanese companies are trying to find out what has made Samsung so successful 
and trying to ape our model of business.
</font></p>
