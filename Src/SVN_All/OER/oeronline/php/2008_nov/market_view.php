				index plummeted 14.9 per cent from September 16 to October 15, 
				2008. MSM experienced the second largest single day fall in 2008 
				with MSM30 index tumbling 7.29 per cent to close at 7141.6 
				points on 7th October. MSM dwindled seven per cent each on 
				October 6, 7 and 8 to close below 7,000 levels first time in 12 
				months.}<br>
				<br>
				Moreover, plummeting crude oil prices and fallouts of global 
				credit crisis weighed on investor sentiments. Nevertheless, the 
				index clawed back to 7000 levels following action by major 
				central banks across the world and the region, introducing firm 
				steps to reduce the liquidity crunch.<br>
