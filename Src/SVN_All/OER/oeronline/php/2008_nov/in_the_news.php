                <font size="4" face="Verdana, Arial, Helvetica, sans-serif">
			Strategic alliances to promote Mirbat </font>
                </b><font size="1" face="Verdana, Arial, Helvetica, sans-serif">
			<br>
			<br>
			<img border="0" src="news1.jpg" align="left" hspace="8" vspace="6" width="300" height="112">Dhofar Tourism Company announced its strategic tie-up with Oman Arab 
			Bank, Bank Dhofar, Hamptons and Wunderman for the development of 
			‘Mirbat Beach’ — the Dhofar Tourism’s landmark project worth more 
			than RO1bn. The Phase I of the project is on the verge of being 
			completed with the construction of the ‘Royal Tulip Resort and Spa’ 
			Mirbat-Salalah, a five-star hotel at the location, the soft launch 
			of which will be held in the first quarter of 2009.<br>
			<br>
			‘Mirbat Beach’ will feature 1,500 residential units (town houses, 
			village houses, villas, apartments) and over 10,000 square metres of 
			retail space and 10,000sqm of office space. The project also 
			features a second five-star hotel with 300 keys, a nine-hole 
			golf-course and clubhouse, a water park, a private hospital/clinic 
			with helipad, nursery and childcare facility, as well as an 
			entertainment centre for adults and children. <br>
			<br>
			Established in 1993 as a public joint stock company, Dhofar Tourism 
			Company works in close association with the Ministry of Tourism in 
			all its undertakings. The tourism development project was initiated 
			with the intention to preserve the existing natural landscape and 
			complement the beauty of the region. Oman Arab Bank (OAB) signed the 
			agreement to partner the project as a financial investment partner 
			through its “Al Dar” housing loans plan, which will aid the purchase 
			of property with loan facilities and financial advice. The alliance 
			with Bank Dhofar was through a memorandum of understanding to avail 
			of its services.</font></p><hr size="1" width="90%">
			<p align="justify" dir="ltr" style="margin-left: 5; margin-right: 5">
			<font size="4" face="Verdana, Arial, Helvetica, sans-serif">
			<b>Paradise of Arabia</b></font><font size="1" face="Verdana, Arial, Helvetica, sans-serif"><br>
			<b>H E Sheikh Nawaf Al Thani, Chairman, Dhofar Tourism Company, speaks 
			to OER about the Mirbat Beach project <br>
			</b>
			<br>
			<b>
			<img border="0" src="news2.jpg" width="150" height="141" align="right" hspace="8" vspace="6">How long have you been working on the blue print of this project?</b><br>
			We have worked on the plan for three and a half years. The master 
			plan was drawn up around a year back. I have been pushing for this 
			project for years as I believe in it and it has taken a lot of 
			effort on my part to convince others who had some reservations about 
			the project. <br>
			<br>
			<b>What makes you so confident about Oman?</b><br>
			Oman has a lot of potential in a number of areas. It can be a 
			business hub because of the attractive tax structure and the support 
			that the government is giving to industry. In tourism the country 
			has a rich historical background, different locations with varying 
			weather conditions, beautiful sun and long beaches. Its geographical 
			location between Europe and Asia gives it an added advantage as a 
			tourism and business hub. <br>
			<br>
			<b>When can we expect the Mirbat Project to take off?</b><br>
			We will wait for things to settle down a bit (in the wake of the 
			financial crisis) and launch it in the beginning of 2009. Once this 
			is done then we will firm up the marketing aspects, price structure, 
			etc, of the project. I would like to add that the Mirbat Beach 
			project has the full support of HM Sultan Qaboos Bin Said and H H 
			Sheikh Hamad bin Khalifa Al Thani, the Emir of Qatar. Salalah is the 
			paradise of GCC. When it is hot in other parts of the region, 
			Salalah has greenery and rains.</font></p><hr size="1" width="90%">
			<p align="justify" dir="ltr" style="margin-left: 5; margin-right: 5">
			<font size="4" face="Verdana, Arial, Helvetica, sans-serif">
			<b>Oman edition of Superbrands launched</b></font><font size="1" face="Verdana, Arial, Helvetica, sans-serif"><br>
			<img border="0" src="news3.jpg" width="300" height="143" align="right" hspace="8" vspace="6">Superbrands’, known worldwide as the ‘Oscars of Branding’, which is 
			an independent arbiter of branding, was launched in the Sultanate by 
			Advertising International Company LLC (ADINC) which will represent 
			Superbrands in Oman. Having successfully launched its programmes and 
			publications in 82 countries across the world, ‘Superbrands’ comes 
			to the Sultanate at an ideal time, when the Sultanate is brimming 
			with business and allied activities.<br>
			<br>
			In Oman, ‘Superbrands’ aims at identifying brands that perform above 
			and beyond other brands within their respective categories. To 
			facilitate this, ‘Superbrands’ will form a ‘Superbrands Council’, 
			which is an independent and voluntary body, made up of notable 
			experts in the branding and marketing communications industry along 
			with some of the sharpest business brains in the country. Only 
			brands that are rated highly by the council will be eligible for 
			inclusion in the ‘Superbrands’ programme. In order to be considered 
			a Superbrand, a brand would have to have established the finest 
			reputation in its field. Brands will be invited to participate in 
			the project based on their market dominance, longevity, goodwill, 
			customer loyalty and market acceptance. Qualifying brands will then 
			be honoured at a tribute event and be invited to participate in the 
			Superbrands book. The book will explore the history, development and 
			achievements of many of the strongest global and Omani brands, 
			revealing extraordinary findings along the way. <br>
			<br>
			The ‘Superbrands’ organisation is acclaimed worldwide as being an 
			independent authority and arbiter of branding excellence and is 
			committed to paying tribute to exceptional brands and promoting the 
			discipline of branding. The ‘Superbrands’ organisation was 
			originally set up in London in 1994 where it published the first 
			Superbrands book. (www.superbrands.com)</font></p>
			<hr size="1" width="90%">
              <p align="justify" dir="ltr" style="margin-left: 5; margin-right: 5"> 
                <font size="1" face="Verdana, Arial, Helvetica, sans-serif"> <b>Recognising 
                branding excellence </b><br>
                <br>
                <b>A few years back you felt that Oman was not ready for Superbrands, 
                what has changed since then?</b><br>
                The pace of economic activity is the big difference. Oman was 
                seen as a sleepy valley some years back, but things have changed, 
                you see a number of neon signs at night with local brand names. 
                This is important as I see places which seem to be economically 
                vibrant but this vibrance is driven entirely by multinational 
                brands and not by local home grown brands. <br>
                <br>
                <b>Do niche brands like a Ferrari or a Lamborghini stand less 
                of a chance of being chosen as a Superbrand than mass market brands 
                like a Pif-Paf?</b><br>
                Lamborghini and Ferrari are aspirational brands but they do not 
                have the same emotional connect like a Jaguar or a Mercedes. So 
                while people may lust for a Ferrari they do not connect to the 
                same extent. <br>
                <br>
                <b>Conventionally, advertising is seen as creating a brand connect 
                or does an emotional connect go beyond advertising?</b><br>
                Advertising can create awareness about a brand but it cannot create 
                an emotional connect for the brand. The emotional connect happens 
                only through its consumption or by experiencing the brand. Every 
                point of contact with the brand from the receptionist to the delivery 
                boy is vastly important for the image of a brand.<br>
                <br>
                <b>How does Superbrands, the concept make money?</b><br>
                We do not accept any advertising so that we remain non-partisan. 
                Even our Superbrand events are not held in any hotel but on neutral 
                territory. We make money by selling the Superbrands book across 
                the world. We do internally rank the brands though the ranking 
                is not revealed publically and the brand which gets the highest 
                score is felicitated as the ‘Superbrand of the year.’ </font></p>

