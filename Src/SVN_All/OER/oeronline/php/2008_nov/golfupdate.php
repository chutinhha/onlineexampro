				watching Jack Nicklaus competing in the 1971 US Masters was a 
				defining moment that inspired him to take up golf. With a set of 
				clubs borrowed from the next door neighbours, Nick started 
				learning golf at his local club. Within four years he was the 
				best amateur player in Europe, winning the English Amateur 
				<img border="0" src="golf1.jpg" width="190" height="434" align="right" hspace="8" vspace="6">Championship and the British Youth Championships in 1975. The 
				following year Nick took up golf professionally. <br>
				<br>
				In 1977, aged just 20, Nick became the youngest ever Ryder Cup 
				team member and proceeded to win all his matches. In the years 
				since, he has represented Europe a record eleven times and to 
				this day he holds the record for the number of points and 
				matches won in the competition.<br>
				<br>
				<b>The</b><b> choker taunt</b><br>
				Throughout the 80s, Nick rose to prominence as one of Europe’s 
				top golfers. Faldo was a steady player who frequently found 
				himself in contention, and he posted wins here and there, 
				leading up to his big 1983 season. But he also developed a 
				reputation as a golfer who couldn’t close the deal in the 
				biggest events. He was derisively called “Fold-o” in some 
				circles, having shown a knack for choking. That’s when he 
				decided to rework his swing. <br>
				<br>
				Under the tutelage of David Leadbetter, he radically altered his 
				swing, a painstaking two-year process that had a revitalising 
				effect on his competitive game. With critics doubting his 
				ability to regain his form, he emerged as a technically better 
				player. The work culminated with his victory at the 1987 British 
				Open, where Faldo made 18 pars in the final round. Nobody would 
				ever again accuse Faldo of folding in big tournaments. He went 
				on to win six Majors including three Open Championships and 
				three US masters. In 1988 a highly successful decade was capped 
				for Nick by an MBE for services to golf.<br>
				<br>
				In 1992 Nick became the first European player to win over £1 mn 
				in prize money during a single season. He held the record for 
				the most successive weeks at the top of the Sony World Ranking – 
				a staggering 81 weeks! The decade ended on a high when he 
				partnered David Carter to England’s first victory in the World 
				Cup of Golf in Auckland in 1998. In all, Faldo won 30 times on 
				the European Tour, 6 times on the U.S. PGA Tour, and 6 majors. 
