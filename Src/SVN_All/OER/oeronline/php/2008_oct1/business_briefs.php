        Renaissance gets PDO contracts </b></font></font>
        <font face="Verdana" size="1"><br>
        <img border="0" src="bus2.jpg" align="right" hspace="6" vspace="4" width="250" height="124">Renaissance Services SAOG has been awarded contracts by Petroleum 
        Development Oman LLC (PDO) to build, own and operate new Permanent 
        Accommodation for Contractors (PAC) facilities in Oman’s interior 
        oilfields at Marmul and Bahja. The long-term contracts run until 2044 
        and the contract value of the initial 5-year period is in excess of 
        RO35mn. Renaissance is investing some RO33mn in establishing the 
        facilities. Once construction and other project contracts are finalised, 
        operations are expected to commence in 2010. The Marmul facility will 
        have 512 rooms and the Bahja facility 304 rooms, with both sites having 
        room for further expansion. The project anticipates an occupancy level 
        of 1,000 residents at Marmul and 600 residents at Bahja. Renaissance 
        already owns and operates ‘PAC’ facilities for PDO contractors at Fahud 
        and Nimr, which opened in August 2000, and Qarn Alam, which opened in 
        February 2002. These three existing PACs have a total of 1,452 rooms and 
        accommodate over 3,000 people. The Renaissance PAC projects won the 
        prestigious Oman Awards for Excellence ‘Investment Project of the Year’ 
        for 2001. Commenting on the important new contract awards, Renaissance 
        Chairman, Samir Fancy says, “Through the PDO PAC initiatives Oman has 
        been at the forefront of raising living conditions and occupational 
        health for workforces. The Renaissance PAC concept and design provides a 
        cost-effective quality solution.”<br>
        <br>
        Renaissance CEO, Stephen Thomas elaborates this point saying, “A 
        Renaissance PAC delivers higher standards at lower costs through 
        economies of scale. The key to the cost-effectiveness of these projects 
        is the predicted likely volume of occupancy and the longevity of the 
        contract period. Residents are safer, healthier and happier in a quality 
        lifestyle environment. The concept can be beneficial to any large 
        constituency of people, including resident workforces, students and 
        military.” The PAC facilities include accommodation with en-suite 
        bathrooms. Recreation facilities including swimming pool, gymnasium, 
        squash, tennis, volleyball, basketball courts and other indoor and 
        outdoor sports and games facilities. There is a fully equipped clinic. 
        Dining facilities are supported by state-of-the-art kitchens, bakery and 
        stores.<br>
