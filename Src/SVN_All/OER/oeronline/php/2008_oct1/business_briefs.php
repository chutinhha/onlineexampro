</font><font size="2" color="#CC3300" face="Verdana"><b>Oman Air welcomes new 
pilot trainees</b></font><font face="Verdana" size="1"><br>
<img border="0" src="bus1.jpg" align="left" hspace="8" vspace="6" width="250" height="126">Oman Air has welcomed a group of 19 Omani Commercial Pilot License-holders who 
successfully completed their “ATPL” (Air Transport Pilot License) Bridge Course 
as an integral part of the Airline Professional Pilot Career Development 
Programme. The course was conducted by Ayla Academy in the Kingdom of Jordan.<br>
<br>
Oman Air believes that among the various positions that are brilliantly handled 
by Omani personnel at various levels, the role of a pilot is the one that is 
most in demand at a global level. The 19 pilot trainees undertook an intensive 
training programme and came back with commendable results. This new batch has 
undertaken several tests and training programmes in accordance with the new 
International Civil Aviation Organisation “ICAO” regulations. The next stage for 
them is to undergo technical courses on Oman Air fleets, safety standards, 
simulation, service manual specification and practice. Trainees will be closely 
monitored until completion of the conversion stage. Oman Air develops tomorrow’s 
career pilot through industry-specific-training-experience with emphasis on 
quality and safety. Each pilot is trained to be an effective member with a 
positive attitude, strong work ethics, sound aeronautical decision-making 
skills, and exceptional airmanship.<br>
