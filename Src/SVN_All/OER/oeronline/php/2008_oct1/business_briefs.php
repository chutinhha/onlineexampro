        Salalah Beach project six months ago and Phase 1 is expected to be 
        completed over the next five years. The mobilisation started six months 
        ago which consisted in preparing the site for construction, in terms of 
        infrastructure, equipment and manpower. Equipment and construction 
        material currently at site includes three excavators, two front-end 
        loaders, one bull dozer and eight tipper trucks - these numbers are to 
        dramatically increase in the upcoming weeks. Temporary access roads have 
        been built and generators installed to speed up construction. Other than 
        three wells, a desalination plant has been set in place that will 
        provide 500 cubic metres of water per day for labour and construction. 
        Phase I would include the Marina, Marina Town, four hotels, an 18-hole 
        PGA golf course and real estate. Currently the focus is on construction 
        of the Marina, two hotels (Mövenpick Hotels &amp; Resorts and Rotana) and 
        the real estate sold in November 2007. The 67 villas and 138 apartments 
        sold in November 2007 will be completed and delivered within three years 
        time and will be set around the vibrant marina promenade and lagoon.<br>
