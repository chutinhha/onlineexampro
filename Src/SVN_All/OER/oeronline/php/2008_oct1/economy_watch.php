GDP at current prices registered a growth of 12.9 per cent in 2007, which though 
reflects some deceleration in relation to 24.8 per cent and 15.5 per cent growth 
achieved in 2005 and 2006, respectively. This growth deceleration resulted from 
a lower rate of increase in oil prices in 2007 in relation to previous two 
years. Despite strong growth in non-petroleum activities, GDP growth behaviour 
still remains dependent on oil price cycle and the impressive progress on 
diversification in recent years has to some extent been overshadowed by the 
large contribution of high oil prices to GDP.</font><p align="justify" style="margin: 3px 10px">&nbsp;<p align="center" style="margin: 3px 10px">
