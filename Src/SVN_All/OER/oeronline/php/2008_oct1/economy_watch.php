The investment needs of an open economy could be financed by both domestic 
savings and inflows of foreign capital. In the case of Oman, domestic savings 
exceed the investment levels, leading to net outflows of financial savings from 
the country for creation of foreign assets abroad. Oman’s gross domestic saving 
as percentage of GDP rose significantly from 34.6 per cent in 2004 to 44.9 per 
cent in 2005 and then fell modestly to 43.3 per cent in 2006.<br>
