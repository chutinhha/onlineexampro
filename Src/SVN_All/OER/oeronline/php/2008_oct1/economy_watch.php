According to the Central Bank of Oman’s annual report 2007, the sound 
macroeconomic environment of Oman in 2007 was reflected in the robust Gross 
Domestic Product (GDP) and employment growth for the fourth consecutive year, 
led by sustained acceleration in activities in the non-oil sectors and 
favourable oil prices. While the inflationary pressures continued, the benefits 
of high growth were visible in major progress on economic diversification, sharp 
increase in per capita income and significant creation of additional economic 
activities for Omanis.<br>
