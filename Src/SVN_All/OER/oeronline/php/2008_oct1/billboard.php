        </font><b><font size="2" color="#CC3300"><font face="Verdana">Oman Oil 
        launches lube plus summer promotion</font></font></b><font face="Verdana" size="1"><br>
        Muscat, August 2008 – Oman Oil Marketing Company (omanoil), one of the 
        leading Omani fuel and lubricants companies, has launched its annual 
        LubePlus Summer Promotion. The first of its kind promotion enables 
        LubePlus customers at select omanoil filling stations to win an 
        exclusive grand prize for two people at the luxury resort and spa Six 
        Senses Hideaway in the governorate of Musandam, perfectly located at the 
        panoramic northern tip of the peninsula. A lucky grand prize winner will 
        be entitled to a number of leisure activities at the indigenous village 
        style resort, including a dhow cruise at Zighy Bay and lavish spa 
        treatments for two. Runner-up prizes include one Toshiba 32 inch LCD TV 
        and one Toshiba Satellite series laptop, courtesy of Bahwan Electronics.<br>
        <br>
        “This marks a new and exciting phase for our wide range of lubricants 
        services, whereby customers will experience our renowned modern and 
        quality LubePlus services in our state-of-the-art facilities, while 
        being eligible to win a number of exclusive prizes”, expressed Shaukath 
        Assadi, General Manager of Lubricants at Oman Oil Marketing Company. <br>
        <br>
        The promotion period will run from August 15 until October 15 2008 and 
        winners will be announced shortly thereafter. Customers that service 
        their cars at one of the LubePlus facilities at a select omanoil filling 
        station will receive a variety of corporate gifts instantly while they 
        wait for their cars to be serviced in addition to a raffle coupon for a 
        prize draw at the end of the promotion. Currently, LubePlus services are 
        available at eight select omanoil filling stations across the Sultanate 
