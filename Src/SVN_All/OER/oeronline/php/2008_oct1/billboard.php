        </font><b><font size="2" color="#CC3300"><font face="Verdana">Areej 
        launches Khafeef frying oil</font></font></b><font face="Verdana" size="1"><br>
        Areej Vegetable Oils &amp; Derivatives (AVOD) launched a new product called 
        Khafeef, the lightest frying oil in the GCC. This high quality oil with 
        its attractive packaging promises to be light in taste, light for 
        digestion and light for health. The product was launched after consumer 
        research and evaluation of different oils in actual frying tests 
        conducted with breaded chicken fillet. It was concluded that no single 
        oil fulfilled all consumer expectations in terms of crisp frying, reuse, 
        lightness, and health.<br>
        <br>
        “It took a few years for AVOD to develop the technology internally. An 
        investment of RO4mn in plant and packaging machinery was further made to 
        deliver the new technology required. The finished product, Khafeef, 
        packaged in a beautiful PET bottle, is all set to revolutionise the 
        market,” says Prem Maker, Executive Director, AVOD. AVOD’s marketing 
        activities will be spread across various media channels such as TV, 
        cinema, press, radio, outdoor and point of sale to further strengthen 
        its brand value in Oman.<br>
