<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
</font><b><font face="Verdana" size="4">Calling it a day </font></b><font face="Verdana" size="1">
<br>
<b>Annika Sorenstam considered by many as the greatest female golfer of all-time 
will be missed on the professional golf circuit once she retires from the game 
by the end of 2008</b><br>
<br>
<img border="0" src="golf.jpg" align="left" hspace="6" vspace="4" width="193" height="411">Annika Sorenstam is one of the greatest female golfers ever – many would say 
she’s the best ever. Combining a cool efficiency with a passionate desire to 
win, Sorenstam was among the best players on Tour from her debut in the mid-90s 
through the remainder of the decade. But as the century turned, Sorenstam went 
on a run of success that rivals anything else ever seen on the LPGA Tour. 
Sorenstam preferred tennis in her childhood, but took up golf at the age of 12 
years. She quickly became good enough to start winning, but was very shy. She’s 
said that she sometimes blew shots in order to finish second and avoid having to 
talk to anyone after a win. Sorenstam attended the University of Arizona where 
she was a two-time All-America selection and co-player of the year in 1991. She 
won the 1991 NCAA Championship and the 1992 World Amateur Championship.<br>
<br>
<b>Life as a professional</b><br>
Sorenstam turned pro in 1993 and was Rookie of the Year on the Ladies European 
Tour. She moved up to the LPGA in 1994 and, although she didn’t win, was Rookie 
of the Year there, too. That first win finally came at the 1995 US Women’s Open, 
and Sorenstam took off on what might be the best career in LPGA history. From 
1995 through 2006, Sorenstam won eight money titles and never finished lower 
than fourth. She won 69 tournaments and 10 majors in that span. Sorenstam was 
one of the best players throughout the mid- to late-90s, winning three times in 
1997, six in 1997, four in 1998, twice in 1999, and five times in 2000. Then she 
rededicated herself to being the best, hitting the gym to add strength – and 
yards to her drives. She befriended Tiger Woods and picked up some of Woods’ 
practice habits; she improved her chipping and putting.<br>
<br>
Sorenstam’s domination from 2001-2005 was complete: she was money leader, low 
scorer and Player of the Year every year. Her win totals included 11 in 2002 and 
10 in 2005. She became one of the longest hitters on the tour without losing any 
of her pinpoint accuracy. Along the way, she became much more comfortable in 
front of the cameras, her public demeanor becoming more confident, and won over 
many more fans.<br>
<br>
At the 2003 Colonial, Sorenstam became the first woman since Babe Didrikson 
Zaharias to play on the PGA Tour. Sorenstam shot 71-75 and missed the cut, but 
earned plaudits for her play and the way she handled the publicity circus. 
Sorenstam suffered a neck injury in 2007 that limited her schedule, and at the 
end of the year she had recorded only her second winless season on the LPGA. <br>
<br>
By early 2008, however, Sorenstam was back, with three wins early in the season. 
However, on May 13, 2008, Sorenstam announced it would be her final season on 
the LPGA Tour - she would leave competitive golf at the end of the year. </font><p align="right" dir="ltr" style="margin-left: 10px; margin-right: 10px">
