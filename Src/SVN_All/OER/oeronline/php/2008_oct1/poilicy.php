      <b>
      <img border="0" src="policy2.jpg" align="right" hspace="6" vspace="4" width="175" height="188">Nasser Khalfan Al Rahbi is senior manager, government institutional corporate 
banking division of National Bank of Oman (NBO) and head of NBO Staff Labour 
Union</b><br>
<br>
      <b>When was NBO’s Staff labour union formed?<br>
      </b>NBO had an employee representative committee (ERC) a few years ago but the NBO 
Staff Labour Union was formed and registered on August 8, 2007.<br>
<br>
      <b>How many employees are members of this union?<br>
      </b>At the time of election, we were 453 members in this labour union. Since then 
the number has grown due to new recruits.<br>
<br>
      <b>What are the aims and objectives of NBO Staff Labour Union?<br>
      </b>Our main objective is to make NBO employer of choice by achieving mutual 
understanding between employees and employer and to band together to achieve 
common goals in key areas.<br>
<br>
      <b>Since the formation of the labour union, has there been any significant work 
done for the welfare of NBO workers?</b><br>
Yes, the NBO Staff Labour Union is recognised and fully supported by NBO 
management. On many occasions the Labour Union representatives have been made 
party to decision making for matters related to employees. This is an ongoing 
activity of Labour Union.<br>
<br>
      <b>What is the future of labour unions in the country?</b><br>
Based on the success of NBO’s Staff Labour Union, I am optimistic that the 
labour unions have a bright future in Oman under the wise leadership of His 
Majesty Sultan Qaboos bin Said.<br>
<br>
      <b>How does the banking sector view the labour unions?</b><br>
From my personal point of view and practical experience, the response to NBO 
Staff Labour Union has been very positive and objective. As such, I am sure 
labour unions in other banks are also enjoying the same trusting relationship as 
well.<br>
<br>
      <b>What is the structure of NBO Staff Labour Union?<br>
      </b>We have five board members and five committees namely legal, HR, research and 
development, collective agreement and welfare. One of the board members chairs 
each of these.</font></td>
