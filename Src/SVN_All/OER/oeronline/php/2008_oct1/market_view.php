<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
</font><b><font face="Verdana" size="4">A minor correction </font></b><br>
<b><font face="Verdana" size="1">Strong fundamentals makes the recent slowdown 
on the MSM a temporary one with the market expected to recover strongly in the 
months ahead </font></b><br>
<br>
<font face="Verdana" size="1">MSM 30 index plummeted 12.5 per cent for the 
period from 16th July 2008 to 15th August 2008, shedding almost half the gains 
of H1 (Half yearly) 2008. Despite strong index earnings growth of 30 per cent 
plus in 2Q08, apprehension over future growth prospects in the region due to a 
drop in crude oil prices and geo-political tension played a major role in the 
fall. Pursuant to which foreign investors were net sellers in the market. 
Average daily turnover decreased by more than 50 per cent to RO11.10mn for the 
period from 16th July to 15th August 2008. <br>
<br>
<b>Sector splits <br>
</b>Industry and services sector index saw significant selling pressure with 
bellwether stocks like Galfar and Oman Cement falling by more than 20 per cent. 
Banking stocks continued to slide down despite strong earnings. However, the 
market recovered from a six month low of 9,419.9 on August 11, 2008. We are 
upbeat about the prospects of Oman given the government’s prudent use of 
petrodollars for long term sustainable growth. Despite the current slide the 
market looks poised for approximately 15 per cent plus growth in FY08.</font><p align="justify" style="margin: 3px 10px">&nbsp;<p align="center" style="margin: 3px 10px">
<img border="0" src="mktview.jpg" width="378" height="372"><p align="justify" style="margin: 3px 10px">
<font face="Verdana" size="1">
<br>
In corporate events, Galfar came up with results for 1H08 and also announced 
restructuring of its organisation to facilitate growth in the coming years. 
Renaissance was awarded a long term PDO contract to build, own and operate new 
Permanent Accommodation for Contractors (PAC) facilities in Oman’s interior 
oilfields at Marmul and Bahja. The long-term contracts run until 2044 and the 
contract value of the initial five-year period is in excess of RO35mn ($91mn).<br>
<br>
In result announcements, Bank Muscat reported 43.7 per cent YoY increase in net 
profit to RO57.8 mn for 1H08. It reported a loan book addition of RO213 mn, up 
seven per cent from 1Q (first quarter) 2008. Oman Telecommunication (Omantel) 
announced a 54 per cent YoY jump in earnings to RO74.8 mn in 1H08. <br>
<br>
<b>Regional indices <br>
</b>The month ended August 15, 2008 witnessed lacklustre performance across the 
GCC bourses and the MSCI GCC Index lost 4.13 per cent to close at 643.37 points. 
The ADSM took the worst hit after MSM and the general index plummeted 8.21 per 
cent. The real estate and banking sector took the worst hit and the indices 
tanked 13.56 per cent and 9.33 per cent respectively. The DSM too was among the 
losers and the general index declined 6.23 per cent, however, it managed to 
secure the position of the second best performing market in the region. The 
banking and the services counters were among the worst performers and the 
respective indices dwindled 7.75 per cent and 5.31 per cent respectively. <br>
<br>
The Saudi Arabian market too ended in red and the Tadawul All Share Index 
declined 5.95 per cent. This was primarily due to new trading laws, export ban 
on cement and geopolitical tensions. The banking and cement counters were among 
the top losers and the indices dropped 6.42 per cent and 6.07 per cent 
respectively. The DFM too saw some selling which drove the general index 
downwards by 5.20 per cent. The real estate sector saw heavy selling due to 
lacklustre performance by Emaar Properties and the index declined 8.11 per cent.
</font>

<p align="justify" style="margin: 3px 10px">&nbsp;<p align="center" style="margin: 3px 10px">
<img border="0" src="mktview1.jpg" width="305" height="666"><p align="justify" style="margin: 3px 10px">
<font face="Verdana" size="1">
<br>
The Bahrain Stock Exchange too lost 3.19 per cent with all the major indices 
ending in the negative territory. The services index was the top loser which 
closed ending down 9.24 per cent. The Kuwait market was almost flat with the 
general index closed ending down 0.76 per cent as compared to the month ago. The 
industrial and the real estate indices were among the worst performers on the 
bourse and closed shedding 2.60 per cent and 1.93 per cent respectively. The 
market managed to secure the number one market in the GCC with the YTD 
performance of 16.38 per cent. The markets corrected slight due to the summer 
effect and minor profit booking, though on lower volumes. Moreover, the 
companies reported decent second quarter numbers and going ahead the similar 
trend is expected. Further, the bottom-line of the companies is expected to 
witness and expansion. We maintain our bullish outlook for the GCC markets as 
there still lies potential in the region. A selective stock picking is 
recommended at the low levels currently.
</font>

