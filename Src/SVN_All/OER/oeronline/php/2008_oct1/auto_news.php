</font><b><font size="2" color="#CC3300"><font face="Verdana">HUMMER WITH TYRE 
PRESSURE MONITORING TECHNOLOGY </font></font></b><font face="Verdana" size="1">
<br>
Four little wheel sensors can help save lives by alerting motorists to low tyre 
pressure, a potentially dangerous driving situation. General Motors is a leader 
in installing Tyre Pressure Monitoring Systems (TPMS) and has installed it in 
four million vehicles. The GM TPMS, available as standard on all Hummer models, 
utilises separate sensors mounted in each wheel. These sensors use real-time 
monitoring to determine the pressure in each tyre. A warning light on the 
instrument panel or a message displayed on the driver information centre along 
with an audible warning, alerts to check the air pressures in the tyre. </font>
