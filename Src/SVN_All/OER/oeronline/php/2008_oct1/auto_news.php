REDEFINING THE FAMILY SEDAN</font></b><font face="Verdana" size="1"><br>
<img border="0" src="auto2.jpg" align="left" hspace="6" vspace="4" width="200" height="131">The Ford Five Hundred is a large family sedan that offers abundance of space, 
class-leading features and smooth revving power. Its rear-seat room is 
particularly impressive. In fact, there’s more rear legroom (41.9 inches) than 
front legroom (41.2 inches). All seats feature what Ford calls a command view of 
the road, with seat-bottom cushions that are some four inches higher than those 
of a typical mid-sized sedan. With plenty of glass all around, outward 
visibility is excellent. The Five Hundred comes with a body structure that 
benefits from the latest in safety technology. The front seats are mounted on a 
cross-body beam that runs from the bottom of the B-pillar (the post between the 
front and rear doors) that adds strength to the car’s shell and helps provide 
protection in the event of a side impact. Powered by a 3.5 litre 286 horsepower 
V6 engine, the Ford Five Hundred accelerates with authority. In Oman, Ford 
vehicles are brought to you by Arabian Car Marketing Co, a part of the Saud 
Bahwan Group. <br>
