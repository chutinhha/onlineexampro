            <font face="Verdana" size="1"><br>
            <img border="0" src="cover.jpg" align="right" hspace="8" vspace="6" width="204" height="169"></font><font face="Verdana" size="4"><b>OMAN 
            BECKONS</b></font><br>
            <font face="Verdana" size="1"><b>Robust economic growth and 
            diversification, has made Oman a destination of choice for a growing 
            number of CEOs and senior executives from developed countries. 
            Visvas Paul D Karra reports</b><br>
            <br>
            Ewan Stirling, CEO, HSBC Bank, Oman has always harboured a desire to 
            come to the Sultanate. A desire stoked by stories about the beauty 
            and culture of Oman that he had heard from his father, a diplomat 
            who had served some of the finest years of his career in Oman. 
            Stirling even went to the extent of writing a letter to HSBC 
            headquarters way back in 1986 to post him to the Sultanate. His wish 
            finally came true in 2007 when he was posted as the head of the Oman 
            operations of HSBC. <br>
            <br>
            <img border="0" src="cover1.jpg" align="left" hspace="6" vspace="4" width="195" height="274">If 
            in Stirling’s case it was all about experiencing the famed Omani way 
            of life, there are scores of others for whom emerging job 
            opportunities is a top draw. Says Richard Russell, CEO, Blue City 
            Company 1, “An increasing number of senior executives in the 
            financial sector from developed countries have been making enquiries 
            about suitable job openings in the region including Oman.” For a 
            long time, the name and fame of Oman spread through word of mouth 
            and it was just tourists who went ga-ga about the beauty of this 
            land. Apart from that, a few westerners headed some Omani companies 
            and the rest of this country was thought to be the bastion for CEO’s 
            and mid level executives from the Asian sub-continent. But now 
            things have changed.<br>
            <br>
            The Sultanate of Oman, which remained a benign neighbourhood in the 
            Gulf region, has now become a hotspot of opportunities. Sound and 
            stable economic development policies put in place under the wise 
            leadership of His Majesty Sultan Qaboos bin Said, have led to rapid 
            economic activities spurred most notably by the diversification of 
            the national revenues from oil to non-oil revenues.</font></p>
            <p align="justify" style="margin-left: 10px; margin-right: 10px">
            <img border="0" src="cover2.jpg" width="450" height="394"></p>
            <p align="justify" style="margin-left: 10px; margin-right: 10px">
            <font face="Verdana" size="1">
            <img border="0" src="cover3.jpg" align="left" hspace="6" vspace="4" width="200" height="255">High 
            oil prices have fattened the treasuries in the Gulf region and 
            prompted investments in infrastructure development and other 
            industrial activities. For example, Oman’s GDP grew by 15.5 per cent 
            in 2006 to RO13,737.7mn. This was followed by a 12.9 per cent growth 
            in 2007 to reach RO15,512mn. There has been a commensurate opening 
            up of new sectors and jobs in the economy (we analyse this later in 
            the article). Meanwhile, regional economies have grown at an average 
            rate of seven per cent over the last five years, spurred by record 
            oil prices and a rapidly growing non-oil sector. <br>
            <br>
            A report by the International Monetary Fund titled ‘Regional 
            Economic Outlook’ for the Middle East and Central Asia published 
            recently says that most of the countries in the Middle East have 
            largely remained unscathed by the recent financial turmoil in 
            developed countries. </font></p>
            <p align="justify" style="margin-left: 10px; margin-right: 10px">
            <font face="Verdana" size="1">The six nations of the Gulf 
            Cooperation Council, which includes energy powerhouse Saudi Arabia, 
            earned $381bn from oil exports in 2007 and an additional $26bn from 
            gas, according to the Washington-based Institute of International 
            Finance. New research by the McKinsey Global Institute (MGI) shows 
            that the oil revenues of the six states of the Gulf Cooperation 
            Council (GCC) — Bahrain, Kuwait, Oman, Qatar, Saudi Arabia, and the 
            United Arab Emirates — will add up to $6.2trn in profits over the 
            next 14 years if crude prices are $70 per barrel.<br>
            <br>
            <img border="0" src="cover4.jpg" align="right" hspace="6" vspace="4" width="200" height="285"><b>Mayhem 
            in developed economies</b><br>
            Oman and the rest of the Gulf countries have not experienced 
            unprecedented scenarios like job cuts and economic downturns which 
            have become the order of the day in the countries across the world. 
            According to Bloomberg, from July 2007 to March 2008 financial 
            institutions laid off more than 34,000 employees across the globe. 
            Hit by the sub prime crisis, Citigroup took a decision in April to 
            lay off 9,000 workers by the end of 2008. This comes on the back of 
            4200 jobs slashed in January 2008 by the financial behemoth. In 
            April, Merrill Lynch said that it planned to terminate 2,900 jobs by 
            the end of the year. At Bear Stearns there were fears that half of 
            the 14,000 jobs could be eliminated once JP Morgan completes its 
            acquisition.<br>
            <br>
            On the other side of the Atlantic in Europe, the banking and 
            property sectors felt the fallout from the financial turmoil in the 
            US. Spain suffered its largest-ever business failure as construction 
            group Martinsa-Fadesa SA, a company with assets of about $17.17 bn 
            filed for bankruptcy protection. The international credit mess has 
            also affected other sectors like manufacturing. Stung by the strong 
            euro and rising costs, Germany’s previously booming manufacturers 
            suffered declining new orders for six months in a row in 2008. <br>
            <br>
            <img border="0" src="cover5.jpg" align="left" hspace="6" vspace="4" width="200" height="272">News 
            reports have indicated that blue-chip companies including 
            engineering giant Siemens; consumer-goods maker Henkel; and 
            printing-machine maker Heidelberger Druck have all announced 
            large-scale job cuts lately. It was reported that Royal Bank of 
            Scotland may cut up to 7,000 job positions worldwide and nearly 
            40,000 workers in the City of London’s financial district are 
            expected to lose their jobs soon.<br>
            <br>
            Despite the weakening global economic climate, the World Bank’s 
            ‘Global Links Report 2007’, says the number of people living outside 
            their home country has more than doubled since 1980 to 190 mn. The 
            World Bank estimates that 43 mn people will enter the MENA workforce 
            in the next decade, compared with the 47 mn total job seekers 
            between 1950 and 1990. A staggering testimony to the latent growth 
            potential and commensurate job opportunities that will be created in 
            the region.<br>
            <br>
            <b>Oman on the map </b><br>
            The job churn caused by the global credit crunch is forcing many 
            senior executives to see a huge vista of opportunity in Oman thanks 
            to the massive developmental activities and stable economic 
            conditions. The hectic economic growth has fuelled a renewed 
            interest in Oman which is seen in the rising number of job-seekers 
            from Europe and other countries jumping ship to move to this 
            country. “Yes, without a doubt as the economic crunch bites into 
            many other regions, the growth in Oman is hugely appealing. 
            International banks and developers are also expanding their 
            blueprints and as resorts and tourist areas drive tourism, it will 
            open vacancies not only for expatriates but also for locals,” says 
            Barry Brown, Vice President Commercial, Oman Air.<br>
            <br>
            <img border="0" src="cover6.jpg" align="right" hspace="6" vspace="4" width="200" height="280">The 
            opening up of sunrise sectors like real estate, hospitality, 
            tourism, manufacturing, IT and telecom have opened up new vistas in 
            the Sultanate. For instance in 2007, non-oil activities registered a 
            robust growth of 18.3 per cent, powered by a 14.3 per cent and 20.3 
            per cent growth in industrial activities and services respectively. 
            Cyril Piaia, CEO, Muriya Tourism Development Company says, “I took 
            on the challenge of heading Muriya operations in Oman, as I saw a 
            lot of growth potential in the country.” Piaia was in Egypt, running 
            his own business before taking up the Muriya offer. Arbind Shrestha, 
            General Manager, Shangri-La’s Barr Al Jissah Resort and Spa echoes 
            the sentiment, “Any executive would be seeking to drive the 
            competitive advantage of his business forward. Oman offers that 
            opportunity. I feel that being in Oman I have the wonderful 
            opportunity to make a contribution to the tourism industry,” says 
            Shrestha.<br>
            <br>
            The vigorous Omanisation drive has continued to provide jobs for 
            locals across all sectors, nevertheless this has not stemmed the 
            flow of expatriates into the country. Infact, there has been a 
            significant double digit rise in the number of expatriates who 
            joined the Omani private sector in the first six months of this year 
            as compared to the whole of 2007. In 2007 there were 638,477
            <img border="0" src="cover7.jpg" align="left" hspace="8" vspace="6" width="214" height="314">expatriates 
            with valid labour cards in the private sector this number went up by 
            13.8 per cent to 726,857 by the end of June 2008. While this figure 
            is an eye-opener, the fact remains that as economies across the 
            globe are reeling under pressures triggered by the sub-prime crisis 
            and its crippling effects, the Gulf region is experiencing buoyancy 
            like never before. Thus it is clearly evident that the oil chase in 
            the Middle East which earnestly began in the middle of the 20th 
            century continues to be the bedrock on which countries, chieftains, 
            chief executives, citizens and sojourners alike have built their 
            fortunes upon. The Sultanate of Oman has indeed emerged as a happy 
            hunting ground for job seekers as well as investors.<br>
            <br>
            “As long as there is a demand for hydrocarbons and you have plane 
            loads of Indians, Chinese, Japanese and the Koreans coming to the 
            Middle East saying we want your oil, there will be a need for people 
            with the right skills,” says Dr Brian Denis Buckley, General Manager 
            &amp; Chief Executive, Oman LNG. <br>
            <br>
            <img border="0" src="cover8.jpg" align="right" hspace="8" vspace="6" width="200" height="280"><b>Human 
            capital</b><br>
            Russell feels that with a huge amount of developmental activity, 
            there is a significant manpower shortage in Oman and global 
            resources would be welcome. Dr Buckley, Brown and Russell are among 
            the list of western expatriates (see table) who have come to Oman in 
            the past two years and occupied senior positions in various 
            companies stretching across the manufacturing, financial, and 
            services sectors.<br>
            <br>
            The Sultanate experienced a 10.2 per cent rise in the expatriates 
            working at administration, directors and managerial levels. While 
            the number in this occupational group was 20,675 at the end of 
            December 2007, it rose to 22,776 by 2008 June end, according to 
            Ministry of Manpower. The unquenchable thirst for requisite skill 
            sets in the private sector is seen in the demand for accountants, 
            business developments managers, financial consultants and people 
            with investment backgrounds. There is a lot of movement in the above 
            mentioned sectors, says Ali Abduladheem, Managing Director, 
            Competence HR, a leading human resources consultancy in Oman.<br>
            <br>
            In a report titled ‘Regional Human Resource Overview’, covering the 
            Middle East, Bayt.com and YouGov Siraj said that western nationals 
            were more likely than other groups to choose to move to another 
            region as an expat. A throwback to the exploratory spirit of the 
            western world. This report also mentions that when asked how they 
            perceive they are faring relative to peers in their country of 
            residence, most people considered their situation to be above 
            average vis-à-vis their peers. Oman as a country came up with the 
            highest satisfaction levels on the survey. <br>
            <br>
            <img border="0" src="cover9.jpg" align="left" hspace="6" vspace="4" width="200" height="295">The 
            general opinion is that Oman is a perfect place to raise the family 
            because of the social fabric of the Omani society. “The kindness and 
            warmth of the Omanis is immediately noticeable. Add to that the 
            spectacular natural beauty, and it makes a very compelling place to 
            live. There are excellent schools and lovely people and I now 
            understand why so many expatriates like to stay here for a long 
            time,” says Giles Cunningham, who took over as CEO, Zubair 
            Corporation eight months back. For Hans Erlings, CEO, Galfar 
            Engineering and Contracting, besides career advancement, the 
            necessary factors that will make a person to consider a job shift 
            are the facilities for children’s education, lifestyle and security.
            <br>
            <br>
            “Lifestyle is totally different from Europe. Western cultural 
            activities are missing, but nature, clean environment, good food and 
            endless desert are attractions to me. Also being situated in a hub 
            of the world makes life more interesting,” says Erlings. One of the 
            most obvious reasons for Oman attracting top talent is its 
            reputation as a peaceful country which has been marching forward 
            driven by a simple but effective vision of His Majesty to develop 
            the country. This and the quality of life offered by Oman sans the 
            stress levels of the so-called developed cities make a consistent 
            but heady combination to draw people to this land which has earned 
            the much-flogged sobriquet of being the essence of Arabia. Says 
            Stirling, “Professionally, Oman has been expanding with the 
            investment levels going up and this is an exciting time for me. <br>
            <br>
            <img border="0" src="cover10.jpg" align="right" hspace="6" vspace="4" width="200" height="301">The 
            rest of the world is waking up to the fact that Oman is extremely 
            desirable. Personally, it is a perfect place for me to raise a 
            family.”<br>
            <br>
            <b>Salary benefits</b><br>
            A zero personal income tax level is another top draw for western 
            expatriates. For example, in the UK the income tax rate ranges from 
            10 per cent -40 per cent based on one’s earning. In the US it is 
            anywhere between 10-35 per cent. A zero income tax regime thus 
            swells up a person’s wallet enormously. Stirling opines that it is 
            not only about how competitive the salaries are here but it is more 
            about tax benefits. The salary levels in the Sultanate have been 
            growing up rapidly adding to its attractiveness. According to a 2007 
            survey by GulfTalent.com Oman registered the biggest salary jump, 
            from 5.6 per cent in 2006 to 11.0 per cent in 2007, driven in part 
            by a 15 per cent pay rise for public sector employees. This also 
            signals the fact that most companies are ready to pay competitive 
            salaries to match those of their counterparts in the other GCC 
            countries, in order to attract and retain top talent.<br>
            <br>
            In the past few years, the country has been witness to significant 
            infrastructure development projects in collaboration with 
            international companies. The Wave, Muscat, has collaborated with 
            Majid Al Futtaim from Dubai; the Blue City has tie-ups with Greek 
            and Turkish companies; while Muriya Tourism Development Company 
            draws on the expertise of Cairo-based Orascom. While all these 
            projects will be boasting of world-class tourism facilities, they 
            require international expertise to manage, execute and finish them 
            on schedule. And these companies are ready to pay top dollar salary 
            for the purpose.<br>
            <br>
            <img border="0" src="cover11.jpg" align="left" hspace="6" vspace="4" width="200" height="275">Meanwhile, 
            existing companies in Oman are also becoming richer and experiencing 
            year-on-year growth with healthy bottomlines. Market capitalisation 
            of companies listed on Muscat Securities Market in 2007 was over 
            $5.2 bn as compared to just $1.54 bn in 2003. This has raised the 
            bar on the ‘affordability factor’ of companies to pay fat salaries 
            to recruit top talent which is necessary to maintain the competitive 
            edge of a business and increase productivity levels.<br>
            <br>
            <b>Global expertise</b><br>
            The ‘look west’ policy of Omani companies also seems to have been 
            spurred by several other factors. The GulfTalent.com survey revealed 
            that, as the supply of skilled staff from traditional markets such 
            as India and Egypt has diminished due to rapid economic growth in 
            developing countries, many employers were looking to newer regions 
            spread across other continents. <br>
            <br>
            Perhaps one of the most important factor is the ability of these 
            managers to bring in their vast experience and provide an 
            international cutting edge to their jobs. Shrestha, for example, was 
            managing the Shangri-La property in Penang, Malaysia before coming 
            to Oman in February this year. He was instrumental in 
            conceptualising the redevelopment plan and repositioning of 
            Shangri-La’s Rasa Sayang Resort and Spa, Penang, Malaysia, which 
            became the leading luxury resort in the region within a year of its 
            re-launch. Says Shrestha “It is this ability to contribute to the 
            economy and not just drawing a large salary that would bring top 
            talent to Oman.” <br>
            <br>
            <img border="0" src="cover12.jpg" align="right" hspace="6" vspace="4" width="200" height="284">The 
            opening up of new sectors has also led to a need to attract 
            experienced executives. Companies in the oil and gas sector like PDO 
            have had a longstanding arrangement with Shell International to draw 
            on their talent pool. Other sectors (and companies) are now 
            replicating this example. Expatriate CEOs are aware of the need to 
            impart their skills to nationals. Dr Buckley says that the first 
            quality of a leader is to identify and nurture future leaders. 
            “There are some aspects which don’t change much wherever you are, 
            like the fundamentals of extracting value for the business through 
            improved governance. Executives have enough experience to try to 
            ensure that the business continues in a safe, reliable, technically 
            and financially secure way. We help the people to achieve that,” 
            says Dr Buckley. <br>
            <br>
            <b>A second innings </b><br>
            A new phenomenon is the coming back of a number of seasoned 
            executives who have served in Oman earlier. They are being drawn 
            back by better and bigger job opportunities. Russell, who was 
            appointed as the CEO of Blue City Company Phase I in July, worked in 
            Oman between 1991 and 1995 and moved to Abu Dhabi and Dubai before 
            his second-coming to the Sultanate. <br>
            <br>
            <img border="0" src="cover13.jpg" align="left" hspace="6" vspace="4" width="200" height="295">Werner 
            Koch, CEO of Al Hashar Group was in Oman between 1985 and 1990 and 
            now is back again from January 2007. Says Koch, “The nature of the 
            economic situation as well as the quality of life in the country are 
            both major determinants. As far as Oman is concerned, the economy is 
            strong and the quality of life is conducive to lead a happy life.” 
            The growing profile of companies in the Middle East is also helping 
            to attract talent to its shores. Trowers and Hamlins won the best 
            law firm in the world award from The Lawyer magazine. The firm’s 
            Middle East practice was singled out by the jury as one of the 
            primary reasons for the award. Presented with a chance to be a part 
            of Trowers and Hamlins Middle East practice, Roger Byrne, partner 
            Trowers and Hamlins was quick to take the plunge. His wife googled 
            Oman (sic) and almost instantly packed up their bags. “Muscat has a 
            lot of characteristics like Brisbane and I suspect a lot of people 
            are moving into Muscat. I was impressed by Trowers and Hamlins and 
            the type of work being done in this jurisdiction,” says Byrne.<br>
            <br>
            The Sultanate of Oman also seems to have become the first choice for 
            those who were looking for a change of experience. In this regard, 
            the efforts being made by the government to promote brand Oman has 
            increased comfort levels of senior executives keen on relocating to 
            Oman. Paul Trowbridge, Deputy CEO, National Bank of Oman who came to 
            Oman two years back, says, “Five to 10 years ago, hardly anyone knew 
            about Oman but now Australians know about this country and
            <img border="0" src="cover14.jpg" align="right" hspace="6" vspace="4" width="200" height="283">they 
            relate to it. The climate is very dramatic and the physical beauty 
            of Oman is very good.” The hidden jewel of the Middle East has 
            finally been discovered. <br>
            <br>
            As a number of home grown companies like Oman Cables Industry, Sohar 
            Aluminium, Galfar Engineering and Contracting, Renaissance Services 
            carve out a bigger role for themselves on the global and regional 
            level, there is a need to attract executives who have a pan regional 
            or global experience. The same holds true for sectors like tourism 
            and real estate. Cyril Piaia, a French national had a choice between 
            Morocco and Oman, “I knew that Oman is a very nice place but with a 
            low profile. But now Oman is developing and has a lot of potential 
            in the next five years and there will be a lot more people coming 
            here.”<br>
            <br>
            To sum it up in Shrestha’s words: “Oman has unique cultural and 
            traditional assets imbedded into the society and the policy of the 
            government is to preserve the nature, culture and heritage intact in 
            its development path. And this is going to be one of the most 
            competitive and key success factor that Oman will have in the 
            future.” While a number of western expatriate CEO’s have come to 
            Oman, with economic progress and development expected to continue at 
            a rapid pace, what we may have seen till now may be the proverbial 
            tip of the iceberg.</font></p>
