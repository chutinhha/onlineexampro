        <p align="justify"><b><font face="Verdana" size="4"><br>
        </font></b><b><font face="Verdana" size="4">Court Failure To Succeed</font></b><font face="Verdana" size="1"><br>
        <b>
        Failure is not a plague to be quarantined, but a life-saving bacteria 
        that needs to be befriended in most cases. No risk, no reward. No 
        failure, no success. Acknowledge failure and we all will be richer<br>
        <br>
        <font color="#990000">By Ramesh Kumar<br>
        </font>
        </b><br>
        <b>
        <img border="0" src="view1.jpg" align="right" hspace="6" vspace="4" width="115" height="152"></b>Globally renowned physician-turned-spiritual guru Deepak Chopra is 
        believed to have exhorted his constituency not to heed to the adage of 
        ‘we learn from past mistakes’. The Chopra prescription was simple and 
        straightforward: Focus on success and don’t waste time on failures. Ever 
        since my colleague Atulya Sharma spilled this Chopraspeak a few weeks 
        ago at a get together – aptly titled ‘Freedom to Succeed’ - I began to 
        ruminate.<br>
        <br>
        Chopra’s utterances are not the gospel truth. It is his personal 
        observation. Fear of failure is a major issue with everyone from all 
        walks of life. Accept it or not, every success is built upon failed 
        initial attempts. It is debatable whether such failures are made public 
        or not. Non-disclosure does not remove its existence. And there is 
        nothing to be ashamed about it. Failures are not a waste of time, energy 
        or effort but sheer learnings and one cannot afford to ignore knowledge 
        and wisdom gained in the process.<br>
        <br>
        <b>Game of Dice</b><br>
        When we embark on a mission/task, of course we have a blueprint - a 
        rough draft of what we want to achieve or where we want to reach at the 
        culmination of this exercise. Yet, there is no guarantee that we may end 
        up with what we aspire for. None except the Almighty can categorically 
        claim such clairvoyance of end results. So, to put it differently, we 
        all take chances with the hope that we will succeed in whatever we do. 
        It is a pure game of dice: sometime, we win and sometime, we lose. <br>
        <br>
        Still the fear of failure haunts us perpetually. We fail often enough. 
        But none of us want to be seen having courted failure. It is a big 
        stigma or embarrassment. Hence we try to brush our failures under the 
        carpet and try to present our successes only. As if we are all made 
        fail-proof. A bit of self-deception and therefore is not a positive 
        trait.<br>
        <br>
        <b>Take Chances</b><br>
        <img border="0" src="view.jpg" align="left" hspace="6" vspace="4" width="150" height="200">Success and failure are the two sides of the same coin. Success results 
        from tinkering with previous failed attempts. Listen to Richard Darkins, 
        the renowned evolution biologist, on taking chances and the route to 
        success. “Chance is a major factor in evolution. It is also a major 
        factor to the success of a product. In species, this chance may be as 
        massive as catastrophic change in the environment or as molecular as 
        genetic mutation. Translating to product management, it simply means: 
        launch. By definition, if a product is never launched, chance can never 
        help it to succeed. Don’t wait for it to be perfect. Get it good enough 
        and start creating opportunities for success. Launch! The worst case 
        scenario is that you fail fast and cheap. In the best case, you survive 
        and begin to evolve.”<br>
        <br>
        Venture capitalist Josh Kopelman of First Capital offers another gem on 
        the road to success: “Ask most successful entrepreneurs how they come up 
        with the idea for their business, and you’re likely to learn that what 
        they initially set out to do is very different from the company you are 
        familiar with. Paypal started out as a service to beam money through 
        Palm Pilots, while YouTube was originally a video dating site.”<br>
        <br>
        If Kopelman were to be believed to succeed in life or business, you 
        cannot afford to be rigid, but flexible. One needs to be ready to tinker 
        with the original roadmap. Paypal and YouTube gambled on their future 
        and reached where they are today. There is every possibility they would 
        have landed belly up with a thud and mud on their back as they were 
        trying to tweak their master plans. They were taking a chance as they 
        firmly believed that not taking a risk is a risk! So when Kopelman says 
        that every business plan is wrong as soon as it is written is a serious 
        declaration based on empirical evidence. <br>
        <br>
        Samuel J Palmissano, Chairman, President and CEO of IBM Corporation, 
        says that, “a focus on innovation works,” in the recent IBM Global CEO 
        Study on ‘The Enterprise of the Future’ conducted encompassing 1130 CEOs 
        spread across the world in collaboration with the Economist Intelligence 
        Unit. Innovation, by nature, is a risky proposition. The enterprises of 
        the future, the study points out, constantly searches for new ways to 
        compete with no guarantee: what is lying at the end of tunnel, none is 
        privy to. That’s life. <br>
        <br>
        When we talk about succeeding and admit that risk is an integral part of 
        such an exercise, failure always lurks round the corner. Failure is not 
        a plague to be quarantined, but a life-saving bacteria that needs to be 
        befriended in most cases. No risk, no reward. No failure, no success. 
        Acknowledge failure and we all will be richer. Deepak Chopra believes it 
        or not, it is immaterial. <br>
