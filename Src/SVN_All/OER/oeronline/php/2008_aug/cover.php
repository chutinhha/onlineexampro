<p align="justify" style="margin-left: 10px; margin-right: 10px"><b>
<font face="Verdana" size="2">
Core competence </font></b><font face="Verdana" size="1">
<br>
<b>Consistent growth has helped Renaissance Services to give its shareholders 
steady returns over the years</b><br>
<br>
Over the last five years Renaissance Services has grown at a compounded annual 
growth rate of 44.2 per cent in revenue and 33.8 per cent in operating profits. 
This growth finds a reflection in its shares which have given a return of over 
222 per cent from July 2005- July 2008. A strategic shift from a diversified 
business model to a focus on its oil and gas business has led to a large scale 
capacity build up. The company has announced an RO195.5mn investment programme 
for the year 2007-09. In 2007 it invested more than RO44.6mn towards this 
programme. The investment strategy focuses on three core initiatives – 
increasing the size and reducing the age profile of the offshore support vessel 
fleet; developing additional capacity in the oil and gas fabrication and ship 
repair business and expanding the capacity and geographical spread of the 
company’s permanent accommodation for contractors (PAC) facilities. The 
investment strategy and its focus on its core oil and gas business promises good 
shareholder returns in future.</font></p>

