<p align="justify" style="margin-left: 10px; margin-right: 10px"><b>
<font face="Verdana" size="2">
Locational advantage<br>
</font></b><font face="Verdana" size="1">
<b>A buoyant economy and enhanced trade activity has helped Port Services 
Corporation to generate value</b><br>
<br>
<br>
Port Services Corporation (PSC) shares have appreciated by 155.2 per cent from 
June 2005 to June 2008. Add the dividend yields given by the company and the 
total returns jump to 170.8 per cent. The company has had a good dividend 
history giving a dividend yield of 7.4 per cent in 2005, 5.1 per cent in 2006 
and 3.2 per cent in 2007. As the Sultanate’s GDP has grown there has been 
enhanced demand for industrial cargo helping PSC post good numbers. Ever since 
its inception in 1976, PSC, the governing body of Port Sultan Qaboos has adopted 
a progressive approach in managing, maintaining and monitoring the entire 
activities of the port. Port Sultan Qaboos, Oman’s premier gateway, enjoys a 
prime location. Situated in a natural harbour 250 kms south of the Strait of 
Hormuz on the Indian Ocean coast of the Arabian Peninsula. Port Sultan Qaboos’ 
location makes it an ideal location not only for the Arabian Gulf but also the 
Indian Sub Continent and markets in the East and South Africa. </font></p>

