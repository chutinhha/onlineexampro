<p align="justify" style="margin-left: 10px; margin-right: 10px"><b>
<font face="Verdana" size="2">
Strong comeback</font></b><font face="Verdana" size="1"><br>
<b>Following its restructuring NBO has emerged as the second largest Bank in Oman 
in terms of size of its loan book<br>
</b>
<br>
<br>
Favourable operating conditions and a benign credit environment helped NBO 
strengthen is asset quality and profitability in 2007. The bank’s growth 
strategy is to be in the forefront of new product development and offerings and 
to invest in its distribution channels to make them more accessible, user 
friendly and customer service oriented. Diversification of revenue streams 
continued to improve with the ratio of other operating income to total income 
improving from 33 per cent in 2006 to 40 per cent in 2007. The bank experienced 
a positive cost of credit in 2007 through collection of non-performing loans. 
NBO was successful in further diversifying its sources of funding by procuring a 
$325mn five year syndicated loan facility from international banks at a 
competitive pricing before to the general increase in market pricing experienced 
in the latter half of 2007. It was conferred with the “Best in Corporate Social 
Responsibility in Oman for 2007”, by the World Finance Magazine, UK. </font></p>

