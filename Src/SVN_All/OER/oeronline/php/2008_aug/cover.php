<p align="justify" style="margin-left: 10px; margin-right: 10px"><b>
<font face="Verdana" size="2">
A promising performance <br>
</font></b><font face="Verdana" size="1">
<b>Oman Oil Marketing has benefited from RISING fuel demand from retail, commercial 
and aviation segment<br>
</b>
<br>
Oman Oil Marketing Company (OOMCO) has given a stock return of 244.1 per cent 
returns over the last three years. The returns are in line with the growth that 
the company has seen from 2004-2007. For instance its revenue has increased by 
115.6 per cent, while its net profit is up by 144 per cent over the period. 
Financial year 2007 saw the company setting new benchmarks – OOMCO recorded its 
best financial performance in the company’s history. It achieved a best 
loss-time injury free record in 15 years. It recorded the highest Omanisation 
rate of 85 per cent and the best collection of debts for the year. The oil 
marketing company also completed a business re-engineering process. OOMCO is an 
Omani joint stock company engaged in the marketing and distribution of petroleum 
products. It has a trademark license agreement with Oman Oil Company for the 
right to use the trademark Oman Oil. Growth of the economy and an increase in 
the number of vehicles on the road bodes well for the stock. </font></p>

