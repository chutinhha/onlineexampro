<p align="justify" style="margin-left: 10px; margin-right: 10px"><b>
<font face="Verdana" size="2">
Placing the right bets </font></b><font face="Verdana" size="1">
<br>
<b>ONIC Holding has created a good portfolio of investments and the results are 
here for everyone to see </b> <br>
<br>
ONIC Holding has given a return of 140.8 per cent. Its turnover has increased 
from RO17.60mn in 2004 to RO35.49 mn in 2007 an increase of 101.65 per cent. In 
May 2008, ONIC took a 25 per cent stake in Sama Holding Company. Sama Holding 
operates in the high growth aircraft leasing business. In March 2007, ONIC made 
a strategic acquisition of 20 per cent of the shares of FrontierAlt. The latter 
is a vertically integrated financial services company in Canada. The company 
offers a choice of opportunities in restructured products, alternative 
investments and speciality investments. Sensing ONICs growth potential a number 
of regional investments firms have kept a keen eye on the company. For example 
Shuaa Capital took a 35 per cent stake in ONIC in September, 2005. Shuaa sold 
its stake to Dubai Financial, the investment arm of Dubai Holding for Dhs52.5mn 
(RO5.25mn) in April 2007. ONIC sold its 35 per cent stak in Alliance Housing 
Bank to Ahli United Bank in June 2007 for RO50.88mn. Thus ONIC has been on a 
roll in the last three years and its share price mirrors this. </font></p>

