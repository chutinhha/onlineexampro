<p align="justify" style="margin-left: 10px; margin-right: 10px"><font face="Verdana" size="1">
<br>
<b>OER-Gulf Baader Capital Markets present a survey of the Top 10 stocks which have 
given the highest shareholder returns over the last
three years </b></font></p>

<p align="center" style="margin-left: 10px; margin-right: 10px">
<img border="0" src="cover2.jpg" width="450" height="244"></p>

<p align="justify" style="margin-left: 10px; margin-right: 10px"><font face="Verdana" size="1">
If you purchased Oman Cables Industry shares worth RO100 in June 2005, the 
investment would have been worth over RO1,200 in June 2008. Surprised, well the 
stock has given a return of 1279.2 per cent during this period. Oman Cables is 
not alone in creating phenomenal amounts of wealth for shareholders. Al Maha 
Petroleum, Shell Oman Marketing, Oman Oil Marketing, Renaissance Services (see 
table) are some of the other companies which have given returns in excess of the 
average returns on the MSM. That’s saying a lot as the local bourse has been one 
of the best performing indices in the region. <br>
<br>
<b>Upward incline </b> <br>
The MSM-30 index closed 2007 with a year-on-year gain of 61.9 per cent. In 2006 
it reported a gain of 14.5 per cent, the highest return in the GCC. This came on 
the back of a 44.5 per cent return in 2005. Abdul Kader Askalan, CEO, Oman Arab 
Bank, states in the bank’s Investment Guide for MSM 30 companies, “We expect the 
market to continue to remain in the positive zone driven by factors such as 
strong oil prices, high cash liquidity in the system, positive currency dynamics 
and strong interest from regional and foreign investors.” <br>
<br>
There is no disputing the fact that MSM has been riding high on the overall 
economic growth in the Sultanate. Oman’s nominal GDP grew by about 8.3 per cent 
to $38.6bn in 2007. Says Ajeev Gopinathan, Senior Research Analyst, Gulf Baader 
Capital Markets, “The last three years has seen rapid economic growth and 
positive macro-economic factors due to high crude oil prices. The fiscal surplus 
has led to government spending on infrastructure. This coupled with capacity 
addition by industry and better corporate earnings have translated into 
shareholder value creation”<br>
<br>
A sectoral analysis of the top ten stocks reflects these assumptions. For 
example manufacturing companies like Oman Cables and Raysut Cement saw enhanced 
demand and capacity additions leading to volume growth and better earnings. Oil 
marketing companies like Shell, Al Maha and Oman Oil Marketing rode the crest of 
rising personal incomes and an increase in fuel demand. Renaissance Services 
+reaped the benefits of servicing the global hydrocarbon industry. While a Port 
Services Corporation serviced a larger number of shipments. Banking and 
investment companies like ONIC, National Bank of Oman and Bank Dhofar benefited 
from greater liquidity in the economy.<br>
<br>
<b>Strong foundations</b><br>
A number of other factors have helped generate shareholder interest on the local 
bourse. The growing number of initial public offerings (IPO) have attracted a 
number of new investors to the market. Omantel’s IPO in June 2005 evinced 
interest from 160,000 investors. Bank Sohar and Galfar Engineering IPO’s in 2007 
were oversubscribed by six and 14 times respectively, raising over RO82mn 
between them. The Capital Market Authority’s efforts to put in a sound market 
infrastructure has created a transparent and fair environment. The introduction 
of online trading last year is expected to attract more retail investors. If you 
are ruing a missed opportunity, maybe it is time to place some bets now, who 
knows three years down you may be laughing all the way to the bank.</font></p>

