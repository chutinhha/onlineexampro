<p align="justify" style="margin-left: 10px; margin-right: 10px"><b>
<font face="Verdana" size="2">
Robust model <br>
</font></b><font face="Verdana" size="1">
<b>A healthy growth in credit demand augmented by the economic cycle has helped 
Bank Dhofar shares during this period<br>
</b>
<br>
Bank Dhofar had a good year with its performance improving on almost all 
financial parameters like total assets, net loans and advances, shareholdrrs 
equity, profits and customer deposits in 2007. The bank was infused with a new 
energy and dynamism. The bank secured a new long term borrowing for US$100mn for 
five years and one month and a new medium term borrowing of US$75mn for three 
years. The first of these will strengthen the capital of the bank and help its 
net worth and accelerate the bank’s asset growth. The facility was jointly 
underwritten by ABN Amro bank and Emirates Bank International. <br>
<br>
Bank Dhofar introduced Oman’s first chip credit card. The chip facility offers 
Bank Dhofar’s card users complete security and greatly enhances the procedures 
of fraud detection at terminals that are chip enabled. The bank was awarded the 
second place in the corporate governance award that was organised by the 
Hawkamah Institute for Corporate Governance and the Union of Arab Banks in 
association with MasterCard worldwide.</font></p>

