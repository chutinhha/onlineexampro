<p align="justify" style="margin-left: 10px; margin-right: 10px"><b>
<font face="Verdana" size="2">
Good going</font></b><font face="Verdana" size="1"><br>
<b>The returns on Shell Oman Marketing stock is a reflection of its market position</b><br>
<br>
Shell Oman Marketing Company remains the unequivocal market leader amongst the 
three oil marketing companies in Oman. It has a 50 per cent market share. The 
company has seen an overall growth in all its business segments -- retail, 
commercial, aviation and lubricants over the last three years. Along with growth 
the company constantly emphasises on an efficient supply chain with a focus on 
margins. All these factors are reflected in the 254.3 per cent return that the 
stock has given from June 2005 – June 2008. The company has also given a good 
dividend yield of 8.7 per cent (2005), 6.5 per cent (2006) and 6.9 per cent 
(2007) to shareholders. The company enjoys a high return on equity and assets. 
This shows how effective the management has been in using its assets to generate 
earnings. Over the last couple of years, it has broken the one billion litre 
mark in terms of retail volumes. The average throughput of bulk fuels per site 
grew by eight per cent in 2007 to 9.3 milion litres per annum. </font></p>

