<p align="justify" style="margin-left: 10px; margin-right: 10px"><b>
<font face="Verdana" size="2">
How We Did It</font></b></p>

<p align="justify" style="margin-left: 10px; margin-right: 10px"><font face="Verdana" size="1">
1. The final rankings were arrived at by adding the capital appreciation and the 
dividend yield given by a company’s share. <br>
<br>
2. Companies with a market capitalisation of RO100mn and above as on June 30, 
2008 were used for the survey. <br>
<br>
3. The survey used data for the period June 30, 2005 – June 30, 2008.<br>
<br>
4. Any company which has not been traded for the last three years was not 
included in the ranking. For example a Galfar Engineering and Contracting has 
given a return of 251.8 per cent but it was excluded from the survey as it has 
been trading for less than a year. <br>
<br>
5. The data was compiled and anlaysed by Gulf Baader Capital Markets.</font></p>

                    <hr width="90%" size="1">

<p align="justify" style="margin-left: 10px; margin-right: 10px"><b>
<font face="Verdana" size="2">
A clear leader</font></b><font face="Verdana" size="1"><br>
<b>Oman Cables Industry has enriched shareholders way beyond their expectations
</b> <br>
<br>
If wealth creation is a major objective for companies then Oman Cables Industry 
(OCI) is its best exponent. Having given a phenomenal 1279.2 per cent stock 
return over the last three years, it is way head of its contemporaries. The 
growth of the company from 2004-07 is reflected in its top line and bottomline. 
Revenues grew by 502 per cent while profits were up by 1006 per cent. The 
company gave a 2:1 stock bonus in late 2006, this was followed by a stock split. 
These two factors further helped shareholder returns. The significant growth 
seen by the company has translated into a growing PE (price earnings) ratio. 
Thus in 2004, the company was trading at a PE multiple of 10.9 which jumped to 
15.4 times in 2008. OCI is one of the top five cable makers in the GCC. OCI has 
a strong local market with over 70 per cent market share. The company exports to 
markets like Japan, France, Germany, US and South Korea. OCI has constantly 
upgraded capacity over the years. </font></p>

<p align="center" style="margin-left: 10px; margin-right: 10px">
<img border="0" src="cover5.jpg" width="450" height="185"></p>

                    <hr width="90%" size="1">

<p align="justify" style="margin-left: 10px; margin-right: 10px"><b>
<font face="Verdana" size="2">
Service Orientation</font></b><font face="Verdana" size="1"><br>
<b>Constant capacity enhancements by Al Maha Petroleum Products has translated into 
higher returns</b><br>
<br>
The growth in the economy over the last couple of years has led to an increasing 
number of vehicles on the roads. This in turn, has led to an enhanced demand for 
fuel in the retail and commercial segments. Al Maha’s retail sales grew by 34 
per cent in 2007, contributing to good revenue numbers. Al Maha’s thrust on 
retail expansion has paid good dividends. The company had 110 filing stations in 
2004, which went upto 151 stations in 2008. This makes it the number two fuel 
retailing company in the sultanate after Shell Oman Marketing. The company’s 
revenue has gone up by 125 per cent from 2004-2007. Its profits over the period 
are up by 166 per cent. The growth in the economy is increasing the fuel demand 
in the commercial segment. This led to an 87 per cent growth in the sale of bulk 
fuels during 2007. The aviation sector has been another contributor to its 
growth. The company has also been introducing value added services in the market 
like Al Maha fuel cards. These factors together have enabled the company to give 
a 310.9 per cent return to shareholders over the last three years. </font></p>

