<p align="justify" style="margin-left: 10px; margin-right: 10px"><b>
<font face="Verdana" size="2">
Capacity build-up</font></b><font face="Verdana" size="1"><br>
<b>A first mover advantage in building capacity and an increase in realisation has 
helped
Raysut Cement to post good earnings growth<br>
</b>
<br>
<br>
Raysut Cement carried out two major expansion projects since 2004 and with 
infrastructure and industrial projects worth US$25bn in the pipeline it is 
sitting pretty. The firming up of cement prices and with domestic demand 
increasing by a healthy 19 per cent the company reaped a virtual windfall. The 
company carried out a four year expansion programme from 2004-07 which increased 
its production capacity from one million tonnes per annum to three million 
tonnes. <br>
<br>
The capacity upgradation and better realisations has helped the company give a 
return of 219.7 per cent. Raysut increased the capacity of its on-shore 
receiving terminals in the port of Mukalla and Aden in Yemen to reach 27,000 
tonnes to cater to this emerging market. A timely increase in the number of 
specialised cement carriers which carry its products to various destinations 
through long term charters helped the company offset the increase in freight 
costs in 2007.</font></p>

