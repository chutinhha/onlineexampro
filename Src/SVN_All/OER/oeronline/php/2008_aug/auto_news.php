<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
</font><b><font face="Verdana" size="2" color="#CC3300">Mazda to cut vehicle 
fuel consumption</font></b><font face="Verdana" size="1"><br>
Mazda Motor Corporation has announced that it is setting its sights on reducing 
the fuel consumption of Mazda vehicles sold globally by an average of 30 per 
cent by 2015. This determined commitment will entail a holistic approach which 
includes using lightweight technologies, the upgrade of almost all of Mazda’s 
gasoline engines, introducing a Smart Idle Stop System, a new gasoline rotary 
engine and new diesel engines worldwide. By 2015, Mazda will have almost renewed 
its entire power train lineup and, from 2011 aims to reduce the weight of its 
new vehicles by 100kg or more.<br>
