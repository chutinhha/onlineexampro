<font face="Verdana" size="2"><b>Regional power of women remains low</b></font><p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1">
<br>
Power Matters: A Survey of GCC Boards produced by The National Investor with the 
support of Hawkamah analyses boards of some 582 companies in the GCC with 3,493 
individuals occupying 4,254 board seats of listed companies in the Gulf. Power 
Matters identifies some of the key trends in the region’s board’s, particularly 
on board composition, the participation of women in boards and the role of 
families on boards.<br>
<br>
Good board practice calls for a manageable board and a board with some 
independence. While our boards are generally manageable, the region still has a 
long way to go in building more independence in the regions board rooms. The 
representation of women on boards is still low compared to international 
benchmarks, despite a growing number of Arab women entrepreneurs. Families still 
dominate some of the boards.</font><p align="justify" style="margin: 3px 10px">&nbsp;<p align="center" style="margin: 3px 10px">
<img border="0" src="eco.jpg" width="309" height="463"><p align="center" style="margin: 3px 10px">&nbsp;<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1">
Out of a sample of 4254 board seats contained only 63 are held by women 
representing 1.5 per cent of the total. This may look miniscule compared to 13.6 
per cent women representation or 22 per cent in Norway. A closer look at the 
data reveals some striking differences between regional peers in the Gulf.</font><p align="center" style="margin: 3px 10px">
<br>
