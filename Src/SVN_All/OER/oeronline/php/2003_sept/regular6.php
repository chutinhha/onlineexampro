            I started my career working in the government sector working for the 
            Ministry of Water Resources in the field of personnel and training 
            management. This work involved putting together a five-year training 
            plan for the ministry, which led to the successful training of 
            qualified engineers from scratch. My civil service career meant a 
            lot of my effort was directed towards the state sector, however, I 
            knew I also wanted to make a tangible contribution to spur training 
            provision in the private sector. Hence, in 1994, I established Al 
            Rakaib Training Institute. It was very different working in the 
            private sector and I soon realized that if I was to realize my 
            vision it would require much more of my time than I had initially 
            anticipated and in 1996 I decided to leave the Ministry to 
            concentrate solely on building Al Rakaib into an institute to be 
            proud of.<br>
            To speed up the development of Al Rakaib Training Institute, I 
            joined forces with Renaissance Services SAOG, a company that 
            acquired a 75 per cent stake in the institute in 1997. The financial 
            support and the talents of the Renaissance team gave the institute 
            more strength to strive towards its commitment and vision of 
            becoming an important tool to deliver better training to meet the 
            skills shortages in the private sector and to boost Omanisation.<br>
            Today, Al Rakaib Training Institute is one of the leading training 
            providers in the region. It was the first retail-training institute 
            to be formed and is now the largest institution in Oman specializing 
            in retail training. <br>
            Training is tailored to suit the needs of a diverse range of clients 
            in the retail/wholesale sector ranging from hypermarkets, 
            supermarkets, convenience stores, grocery stores, distributors, 
            bookshops and department stores in Oman. Most of the major high 
            street players have employees trained by Al Rakaib. Last year Al 
            Rakaib branched out and started retail training for a major client 
            in the UAE. By using our expertise and working closely with our 
            clients, we have designed many specialist programmes and 
            qualifications to suit both local and international 
            standards/Awarding Bodies. We have developed training programmes for 
            three of the five franchisers involved in the SANAD programme, and 
            BTEC qualifications including a BTEC Cashier Certificate. The 
            training of Omani forecourt attendants was also the brainchild of Al 
            Rakaib and remains of great importance to me. Al Rakaib has trained 
            attendants for BP, Shell and Al Maha.</font></p>
            <p><font face="Verdana" size="1"><b>If you could change the 
            education system in Oman, what modifications would you make?</b><br>
            I would add a lot more emphasis on career counseling. Students need 
            to have access to more information on careers to help them decide 
            which career path to follow and to give them a broader understanding 
            of what requirements are needed for particular professions. They 
            need to be able to identify their own strengths and weaknesses and 
            be clearer about what they want to do for a living. This information 
            should be given as early as possible in schools. Also, I would 
            strengthen the link between vocational training and education in 
            Oman. A better bridge between all educational institutions and 
            training centers for Omanisation must be created.<br>
            <br>
            <b>Are your training methods tailor made for Oman?</b><br>
            Essentially, we employ methods that meet international training 
            standards. However, we have learned that certain methods are more 
            effective than others when training Omani candidates and have 
            developed our training methods accordingly. What is most important 
            to us is that individual needs are met wherever possible. <br>
            <br>
            <b>What is the most difficult thing to teach?</b><br>
            I believe that instilling work ethics is the most difficult thing. 
            Candidates need to clearly understand what is 
            acceptable/unacceptable behaviour in the institute and the 
            workplace. I don’t know many employers who will tolerate poor 
            timekeeping, non-attendance with no good reason, lack of respect, 
            and lack of commitment to their job. Candidates need to come with 
            the desire to learn and to improve.<br>
            <br>
            <b>Is enough attention paid to vocational training here?</b><br>
            There has been a lot of focus given to vocational training in the 
            past 30 years. The vocational training sector has faced substantial 
            changes and is now in need of stability and long term planning to 
            ensure that the training providers perform to professional standards 
            and that all training is more accurately linked to future job needs. 
            We are certainly on the right path and the Omanisation results 
            already prove this. One must not forget that over half of all Omanis 
            working in the private sector have received some form of vocational 
            training.<br>
            <br>
            <b>How do you monitor the performance of the Institute?</b><br>
            I open my eyes and ears and remain open to all comments. On a more 
            serious note, we have very tight management systems in place to 
            check performance. Meetings take place on a regular basis at all 
            levels within the institute where every aspect of the institute’s 
            performance is critically examined. Training reviews are carried out 
            regularly and course contents are adjusted accordingly. Most 
            importantly we conduct client evaluations (for both students and 
            employers) on a regular basis and we then action their feedback.<br>
            <br>
            <b>Trainers have their ups and downs, what are yours?</b><br>
            Inefficiencies drive me crazy. My worst grievances are when 
            something is being implemented too slowly because of inefficiency or 
            when someone lets everyone in the team down. On the other side, I 
            like the challenge of creating new ideas that can be applied 
            locally. <br>
            <br>
            <b>What would you say to a trainee who is about to fail one of your 
            vocational courses?</b><br>
            In vocational training, there is no pass or fail. The programmes are 
            based on competency and it is our role to continuously develop the 
            student to enable them to be competent in their job. If trainees 
            have problems with the course work they will receive extra support 
            and training. It is up to us to ensure that we are providing them 
            with pastoral support through our tutorial system and our Student 
            Welfare Officer to ensure that external problems do not interfere 
            with their training.<br>
            <br>
            <b>What happens to the trainees after they have completed their 
            training at your institute?</b><br>
            Currently, we train Omani candidates to enable them to enter the 
            workplace at entry level. Many of our ex-students remain in close 
            contact with the institute and staff which provides us the 
            opportunity to continuously receive feedback from the field and 
            improve our training accordingly. Beyond that, the company that 
            employs them has a duty to train and develop them further as it is 
            in every company’s interest to improve their staff to raise 
            productivity and to improve profitability. Some of our former 
            candidates are now at supervisory level.<br>
            As an Omani, I look forward to the day when we will see Omanis at 
            every level in the workplace and will feel proud that Al Rakaib 
            played a small part in that achievement. n<br>
