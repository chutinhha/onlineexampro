                  <td width="100%"><font face="Verdana" size="4" color="#FF0000">The power of convertibles</font><p><font face="Verdana" size="1"><b>Finance major United Finance Company is entering the market to raise RO5 million through corporate bonds. OER examines what's in it for the investor&nbsp;</b></font><font face="Verdana" size="2"><br>
                    <br>
                    <img border="0" src="../../images/2002_september/38-murthi1.jpg" align="left" hspace="5" vspace="5">If you wish to know the value of money, go and try to borrow some," whoever said this must have been a pessimist. The United Finance Company (UFC) is going to the market to mobilise RO5 million (US$13 million) with the first-ever public issue of corporate bonds and is expecting that the issue will have an overwhelming response. The instrument will certainly not be the only investment opportunity of its kind in the Sultanate. There are BankMuscat bonds, which were rights issue and are currently listed in the Muscat Securities Market (MSM). They were brought out at the time of the merger of the erstwhile Commercial Bank of Oman and the Industrial Bank of Oman. There are also government development bonds that are floated frequently and which attract
                    over subscription.<br>
                    <br>
                    <b>The optimism</b><br>
                    UFC is optimistic about its bond-issue on the basis of three factors - firstly, the legacy of their 1997 public issue having been oversubscribed by 109 times for the first time in the history of the country's capital market; secondly, the company has one of the healthiest balance sheets; and thirdly, it has significant advantages over the BankMuscat bonds. Raghavan K. Murti, General Manager, UFC, declares, "Our product gives more security, options and yield to the investors at a time when they are being very cautious in a volatile international market."<br>
                    Murti believes that the attractiveness of the bond will be boosted by the options to convert&nbsp;</font></p>
                    <ul>
                      <li><font face="Verdana" size="2">33.33 per cent of holding on com- pletion of 36 months from the<br>
                        allotment date.&nbsp;</font></li>
                      <li><font face="Verdana" size="2">50 per cent at the end of the 48th month.&nbsp;</font></li>
                      <li><font face="Verdana" size="2">100 per cent after 63 months.&nbsp;</font></li>
                    </ul>
                    <p><font face="Verdana" size="2">However, if an investor chooses not to convert, then the eligible bonds shall be listed as non-convertible and redeemed on maturity. In comparison, BankMuscat bonds are automatically converted to equity after their maturity.<br>
                    <br>
                    <b>The yield</b><br>
                    Holdings of UFC bonds may end up yielding more than those of BankMuscat. According to National Bank of Oman (NBO) officials, UFC is offering an assured 85 per cent of the weighted average closing price in the MSM over the three-months preceding conversion, subject to a minimum of 100 per cent of the last audited book value per share. BankMuscat bonds, in contrast, attract just the weighted average closing price for the three preceding months. The investor will not get any dividend that may be declared within that period and would also lose out on interest from the date of recording for conversion to the date of payment.&nbsp;<br>
                    This is where UFC scores over BankMuscat. Emphasising this point, Murti says, "We have protected the investor with 15 per cent discount even if there is to be a downward trend in the market." The company will also pay any dividend that may be declared during the three months after date of recording conversion. Moreover, the investor will earn interest for three months from date of recording conversion till the date of payment.<br>
                    UFC also scores on security. The bondholders' claim over the assets of UFC, in the event of bankruptcy of the company, will rank pari passu with those of other secured creditors of the company (in this case, three commercial banks of Oman). Muscat Depository will work as a trustee to the investors for execution of the charges.<br>
                    <br>
                    <b>The final call</b><br>
                    It appears that UFC's offer is heading for an overwhelming response by the time subscription closes on September 15, 2002. As V. Ramakrishnan, Divisional Manager, Investment Banking Division, NBO, puts it, "There is adequate coverage for investors with a long-term BBB (oma) rating." The prospects for this bond are bright since UFC is in the business of leasing and auto finance. Murti declares, "We have still not found any downturn in the robust leasing sector, even when there was a market crash between April and June 1998. And Oman has a huge demand in the transport sector." It now remains to be seen how the issue can bond with the investor.</font></p>
