                  <td width="100%"><font face="Verdana" size="4" color="#FF0000">Office
                    World</font><p><font face="Verdana" size="1"><b>It is only a matter of time before things start getting better for the office rental industry in Muscat, Nayl D'Souza reports</b></font><font face="Verdana" size="2"><br>
                    <br>
                    <img border="0" src="../../images/2002_september/35-PIC.jpg" align="left" hspace="5" vspace="5" width="225" height="136"></font><font face="Verdana" size="2">In September, top business managers and government officials return to Muscat from their summer vacations. While a few may argue that the absence of these decision makers was not really felt; the city was noticeably quiet without them. Business was down. And this trend was mirrored by the commercial real estate market in Muscat, which has been persistently plagued by weak demand and oversupply. Despite the gloom, industry officials have identified a few trends, at the very least, which indicate that the worst might soon be over.<br>
                    "In some places, office rents are much lower than those that existed five years ago," says Michael Lowes, General Manager, Cluttons, an international property consultancy. "The peculiarity of Muscat when compared to other Gulf cities is that a high percentage of offices are occupied by owners themselves. International companies, which have smaller presence in Oman, often tend to operate through their regional offices in Dubai, Abu Dhabi or Manama."<br>
                    A comparison of office rents across the Middle East carried out by Cluttons some time ago indicates that Muscat had lowest rents even after adding local utility charges and removing government levies and service charges, which other cities had to contend with. The results of the aforesaid comparison hold true even today. Cairo and Beirut still achieve the highest rental values for prime and secondary office space, followed by Dubai and Abu Dhabi. Muscat's commercial rents that currently average between RO3.0 to RO3.5 per square metre per month are still 40 per cent cheaper than Dubai (a market also suffering from oversupply), where rents are beginning to soften after an incredible construction boom and moderate absorption rate recently.<br>
                    Hamptons International, another real estate consultancy, estimates that it could take at least two years for the present supply of office space to get completely absorbed provided the demand continues to exist at the current rate and no new office buildings are erected.<br>
                    "The lull in demand over the past few years has brought down the rental levels in Muscat making it a least expensive city in the world," says Christopher Steel, General Manager, Hamptons International. "I think that the present rentals throughout Muscat are as low as they can possibly go."<br>
                    He explains that office rents have reached their critical point where any further rental decline will make office development economically unviable. So no major office construction is likely to take place until the existing excess supply is reduced.</font></p>
                    <p><font face="Verdana" size="2"><img border="1" src="../../images/2002_september/35-GRAPH.jpg" align="left" hspace="5" vspace="5" width="156" height="140">Latest research by Hamptons International indicates that Muscat has approximately 9,000 square metres of vacant commercial space available, ready to be occupied. It is estimated that an additional 12,000 square metres of new speculative office space will be released into the market before the end of this year. It is projected that 35 per cent of all new commercial stock may remain unoccupied. Renting of office space in recent months has been high, although this figure could be viewed as misleading since existing tenants shift to new locations.<br>
                    <br>
                    <b>Changing tenant preferences</b><br>
                    Several new office buildings have already been completed including Muscat International Centre (MIC) in the Central Business District (CBD) area, adding around 8,000 square metres of space. "There are signs that the new buildings in the market are being taken up. Once this happens, this may lead to improved rental rates," adds Lowes of Cluttons.<br>
                    However, Hamptons' Steel observes that a new demand sector has evolved in Muscat where existing companies in the CBD are now able to relocate their offices to nearby brand new all-purpose built premises at a cheaper rent than they had previously been paying. "The landlords will try to prevent tenants from moving on by offering incentives like reduced rent, but a two-tier market is almost certainly evolving." As time passes, it seems that the newer, higher, international quality properties will retain their value and the older lot might see their rental price erode.<br>
                    Another key trend Steel identifies is that there has been a sustained movement of companies from the CBD area to outer lying areas like Qurm and Al Khuwair over the recent years. Rents in the established CBD area are now generally lower than the outer lying areas. "It is predicted that CBD will retain its importance due to the banking and commercial sectors but the outward shift will continue towards Azaiba," he adds. The primary reason for this shift is the severe lack of adequate car parking space in the traditional areas. Abundant car parking is now seen as an important factor in the success of commercial premises in addition to the environmental factor, which also plays an important role in the decision-making process.<br>
                    <br>
                    <b>Location matters</b><br>
                    Other tenant preferences exist for choosing an office location such as ensuring a good degree of staff comfort. This takes into account health and safety aspects in addition to considering how the office location can assist employee productivity. Furthermore, tenants look at taking an optimum unit size. Clients currently demand office space units in multiples of 120 square metres, according to Steel, although Hamptons has recently leased 1,000 square metres floors to the established companies. Modern data telecommunication (underfloor trunking for data and communication ports) is also de rigueur. "It's as important as decent air conditioning systems," adds Steel. Finally, locating offices in commercial premises is a new trend nowadays and is seen as a major drift from multiple-use buildings.<br>
                    Also, with Oman's accession to the World Trade Organisation, foreign ownership is expected to be allowed in the real estate sector, besides relaxing regulations on visas and company registration. Till then, there might be no significant shift in the fundamentals of the sector, despite the annual 10 per cent yield on investment accrued from commercial and residential properties.</font></p>
