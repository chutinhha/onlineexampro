                  <td width="100%"><font face="Verdana" size="4" color="#FF0000">Shopping bliss</font><p><font face="Verdana" size="1"><b>The Markaz Al Bahja shopping complex plans to open in less than three months from now.&nbsp;</b></font><font face="Verdana" size="2"><br>
                    <br>
                    </font><img border="0" src="../../images/2002_september/46-markaz.jpg" align="left" hspace="5" vspace="5" width="225" height="161"><font face="Verdana" size="2">Markaz Al Bahja, the much-awaited RO10 million shopper's paradise promoted by Ajay Group, is making final preparations for a grand opening on 18 November 2002. A list of some of the mall's tenants released in the local press recently has fuelled enormous customer interest and sounded alarm bells for competing retailers.&nbsp;<br>
                    No doubt, the facilities within the shopping complex are the talk of the town. But a lot of the sheen of Markaz Al Bahja has been taken away with the opening of the rival shopping complex Muscat City Centre in October last year. The real test for the complex will be in how much business its tenants can generate. However, the officials concerned with the project insist that it will be a big hit.<br>
                    "Markaz Al Bahja will offer Oman something unique. It has been designed by architects who have earlier built successful international shopping centres," says Alan Fobian, General Manager, Markaz al Bahja.<br>
                    <br>
                    <b>The hitches</b><br>
                    <img border="0" src="../../images/2002_september/46-fobian.jpg" align="right" hspace="5" vspace="5" width="122" height="200"> However Markaz falls behind City Centre in size. It is a three-level complex with a total shopping area of about 32,500 square metres, which is close to a quarter less than the sales space at City Centre. The mall will have parking space for about 800 cars (including a basement to accommodate about 180), which is close to half the parking available at City Centre.&nbsp;<br>
                    During the first month of operation, Markaz Al Bahja will aim to average 20,000 visitors per day. But industry observers say that the shopping complex may fall short of necessary parking space for customers. City Centre experiences a rush exceeding its capacity during the weekends. However, Fobian doesn't think that this would pose a major problem. "A major advantage of our location is the massive amount of off-street parking available." He also highlights that customers will soon be divided between City Centre and Markaz Al Bahja.<br>
                    The ground floor of Markaz Al Bahja houses most of the mall's shops including the 3,000 square metre anchor store called ID-design, a leading Danish home interiors chain. Similar to Sweden's Ikea, ID-design will retail home furniture, accessories and fittings with the unique selling proposition of 'Scandinavian designs at affordable prices'.&nbsp;<br>
                    Officials indicate that the deferment of inauguration of Markaz Al Bahja - originally planned to open at about the same time as Muscat City Centre - was due to the delay in finding a suitable anchor tenant.&nbsp;<br>
                    Fobian says, "Three thousand square metres for a supermarket would not leave an impression in the local market with the likes of Carrefour in town. We needed to find an international trader that was unique. A brand that wasn't a supermarket but that could still anchor a mall of this size and type. That challenge consumed time and caused the delay but I'm very pleased with ID-design." Now our priority is to conclude negotiations for a convenience store. "We are talking to a number of players," he adds.&nbsp;<br>
                    <br>
                    <b>The local flavour</b><br>
                    Evidently, Markaz Al Bahja has modified its strategy for selection of the other stores that will be housed in the complex. Originally the mall had intended to go as strongly as City Centre on international brands, but this policy has been revised. Fobian says, "Given what we've learnt from City Centre's experience, we will give our mall more of a local flavour. Established and popular brands like Capital Stores, Ali Sulaiman Trading and Al Faisal Perfumes have been roped in. We feel it's an exciting prospect for the local traders, giving them the opportunity to compete with international brands."<br>
                    Other local companies on-board include Muscat Pharmacy, Shoe Palace International and Ajmal Perfumes. Group Retail Manager for Ajmal Perfumes, Rami Thabit, says that he is looking forward to opening a store in Markaz Al Bahja and does not regret the decision to not take up space in City Centre. For him, Markaz Al Bahja offers the locational advantage. Ajmal Perfumes looks to expand its current network of 12 stores around the country.&nbsp;</font></p>
                    <p><font face="Verdana" size="2"><img border="0" src="../../images/2002_september/47-shopping.jpg" align="left" hspace="5" vspace="5" width="225" height="172">Nevertheless, a good international cross-section of brands will be on offer as the complex is planning to launch Levi's of the US, Mesuka Fashion Sports of Japan and Bombay Store, a home interiors chain. According to the company management, Markaz Al Bahja will generally cater to top and middle-income customers, with the latter accounting for a larger share. The combined international representation among the stores at the Markaz Al Bahja and City Centre, will offer a good alternative to those locals and expats who currently prefer to go for shopping in Dubai.<br>
                    <br>
                    <b>Shop 'n' fun&nbsp;</b><br>
                    Besides giving a complete shopping experience, Markaz also has plans to entertain kids, families and shoppers alike. There will be two cinema screens in the basement, coupled with seating capacity for 166 people. There will be a ten-pin bowling alley with eight-lanes which can host international tournaments. "We have plans of conducting official leagues and making ten-pin bowling a big sporting focus in Oman," says Fobian. There will also be a snooker, billiard and pool zone for youngsters, fitted with about 20 tables.&nbsp;<br>
                    <br>
                    An entertainment zone on the top floor will be run by Bahrain-based Foton. It will have bumper boats, a roller coaster, a climbing wall and all kinds of other playing equipment for kids in about 2,000 square metres of area. "Shopping is no longer just about buying and walking straight out of the arcade. It has become an outing for the whole family," says Fobian. But Markaz is likely to face competition on the entertainment front too. The second phase of City Centre, which is expected to begin construction before mid-2003, will see the expansion of its existing entertainment offering Magic Planet. It remains to be seen which way customers will 46turn.&nbsp;</font></p>
