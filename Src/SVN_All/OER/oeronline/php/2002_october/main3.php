                  <td width="100%"><font face="Verdana" size="4" color="#FF0000">Saving craze</font><p><font face="Verdana" size="1"><b>Jamaiya is emerging stronger in Oman as an informal financial cooperative. But without a regulatory framework, this trust-based financial scheme has many challenges ahead.</b></font><font face="Verdana" size="2"><br>
                    <br>
                    </font><img border="0" src="../../images/2002_october/personal-fin.jpg" align="left" hspace="5" vspace="5" width="225" height="149"><font face="Verdana" size="2">A new breed of financial organisations is catching mass fancy in Oman. If a survey in Al Dhakhliya region is anything to go by, a large number of households are participating in Jamaiya, a type of financial cooperative.&nbsp;<br>
                    It works like this: Each Jamaiya constitutes a group of members who pay a fixed amount every month for a fixed term. At some point during the term of the scheme, the member will receive an amount equal to the sum of all his monthly contributions during the total duration. The amount contributed equals the amount received. No interest is awarded to members' contributions nor is any interest charged on the amount of money the member receives from the Jamaiya.<br>
                    OER surveyed 14 Jamaiya. Although none of the participants were willing to go on record, the results were profoundly interesting. The Jamaiya came in different shapes and sizes, and with durations ranging from one to 10 years. The members' contribution also started from as little as RO10 per month to RO1,000, though some Jamaiya impose an upper limit on the monthly contribution.&nbsp;<br>
                    <br>
                    <b>The origins</b><br>
                    Jamaiya started appearing in Oman in 1998 with groups of 10 to 40 members contributing between RO10 to RO100 per month for a period of 12 to 30 months. Both the membership and duration were fixed. A monthly contribution of RO10 for 30 months would get the member a total amount of RO300. In case of a monthly contribution of RO100, the total payment will be RO3,000.&nbsp;<br>
                    This money would come in handy for household expenditures. The members typically came from the same neighbourhood, workplace or place of study where the relational bonds between people are strong, levels of trust are high and the risks of default on payment are low. Therefore, there was no need for a written contract when joining the Jamaiya or any forms of guarantee after receiving the amount. Some of these Jamaiya have proceeded to a second cycle after successfully completing the first one.<br>
                    Larger Jamaiya started emerging from 1999, after the success of the smaller. Contributions now even went upto RO650 or more per month, with members committing to make monthly contributions for 10 years. In this case, members would receive RO78,000 at the end of the term. These larger amounts of money will allow members to buy or build house, purchase a car, fund wedding, or for even business start ups, which the smaller Jamaiya could not service.<br>
                    Large amounts of money can be obtained only in two ways - by increasing the size of the monthly contribution or by extending the duration of the Jamaiya.&nbsp;<br>
                    If one wanted RO30,000 to build and furnish a house he would need to contribute RO1,000 per month in a two and a half year long Jamaiya.&nbsp;<br>
                    Alternatively he could pay RO250 per month in a ten year Jamaiya. However, in 10-year option one may have to wait for a longer period to get his money, may be the full 10 years. This has prompted Jamaiya to evolve newer designs.&nbsp;<br>
                    <br>
                    <b>Unlimited membership&nbsp;</b><br>
                    Whereas the small Jamaiya have a fixed number of members and a fixed term, the larger Jamaiya have opened up their membership to unlimited numbers and also allowed enrolment every month. Each member will be in Jamaiya for 10 years, making 120 regular monthly payments. For instance, in some cases a membership run from October 2002 to September 2012, whilst for others from March 2003 to February 2013.&nbsp;<br>
                    The more members join Jamaiya, the sooner the original members receive their money. For instance (see table), ten original members contribute a total amount of RO1,500 per month and receive their amount on the basis of their membership order. The first member receives RO3,000 in the second month, the second member receives RO12,000 in the 10th month and the 10th member receives RO12,000 in the 120th month.<br>
                    Then at the start of the second year of operation, 10 new members join the Jamaiya, thus swelling the total monthly contribution to RO3,000. This means it takes only half the time for the next next in the order to receive his money. This reduces the total length of wait for members ranking from the orders three to 10.&nbsp;<br>
                    <br>
                    <b>Minimising risks</b><br>
                    There are also risks involved in Jamaiya, especially in bigger ones. Larger membership of Jamaiya means the bond between the members will be weaker. The levels of trust will be lower and the risk of defaulting on repayment after receiving the amount will be higher.&nbsp;<br>
                    However, some of the larger Jamaiya are seeking to maintain high levels of relational proximity between members and thus maintain low levels of risk, by categorising them according to tribe or family. One large Jamaiya may be made up of ten sub-groups, within which people know each other well.<br>
                    A plenty of measures are adopted by Jamaiya to minimise risk. For instance, after receiving a membership application, the Jamaiya committee verifies the person's trustworthiness and financial situation.&nbsp;<br>
                    A guarantor will be required to underwrite the member's debt should he default on contributions. Either on joining the Jamaiya or on receipt of his amount, the member will give an immovable asset such as a house, commercial building, or land as a mortgage.&nbsp;<br>
                    When a member receives his amount, he has to go to the local court and sign a legal contract (sakk shariay) saying that he owes the founding members the sum of their remaining monthly payments. He also gives the founding members a signed cheque for each of the remaining monthly payments.&nbsp;<br>
                    <br>
                    <b>Pyramid Vs Jamaiya</b><br>
                    Jamaiya are fundamentally strong. However, many associate Jamaiya with pyramid schemes, whose design is probably unsound. The pyramid schemes encourage you to contribute an amount of, for example, RO1,000 and return you RO5,000. What you get back is much more than what you have put in, hence the attraction. The payment of higher returns to the existing members depends completely on the contributions of new members. Therefore, the pyramid scheme's liabilities (what they owe) are always greater than their assets (what they have).&nbsp;<br>
                    However, this is never the case with Jamaiya where assets (contributions) are equal to liabilities (amount paid out). New members do benefit existing members by reducing their waiting time, but the payment to existing members does not depend entirely on the new members. It's only that the members who join last will have to wait longer to receive their amount, sometimes up to the end 10 years. Also, the design of the Jamaiya is such that the last members will receive their amount from the ongoing contributions of the earlier members. This is very different from the pyramid schemes.<br>
                    All the Jamaiya OER interviewed accepted that there is risk involved in their organisations. The possible risk is of members defaulting on their payments, especially after they receive their amount. However, none of them said they have had this problem yet. But then most of them have been running for only less than two years.&nbsp;<br>
                    The reaction from the banking sector is mixed. A spokesman from Alliance Housing Bank said Jamaiya are 'an alternative source of finance to people and hence are welcome'. However, Oman Arab Bank's Executive Director Abdul Qader Askalan has a different view. According to him, Jamaiya is very risky because there is no regulatory control over them.&nbsp;<br>
                    However, people are interested in Jamaiya because it is easier to get a loan without going through loan application procedures, and the loans are cheaper as well. But the lack of legal and regulatory framework may make Jamaiya prone to risks.&nbsp;</font></p>
