                  <td width="100%"><font face="Verdana" size="4" color="#FF0000">Picture this</font><p><font face="Verdana" size="1"><b>A host of new top-line cinema halls are expected to redefine the entertainment industry in Oman. Nayl D'Souza analyses the trends</b></font></p>
                    <p><font face="Verdana" size="2"><img border="0" src="../../images/2002_october/cinma-2.jpg" align="left" hspace="5" vspace="5" width="225" height="153">The two most successful cinemas in Muscat report 2002 as a profitable year for them. Jawad Sultan Enterprises LLC (JSE), which operates Al Shatti Plaza cinema in Qurum and Al Nisr cinema in Ruwi, has been able to maintain good billings - albeit in a market of less than a dozen cinemas across the country - by offering maximum comfort to their customers and by running hit movies.&nbsp;</font></p>
                    <p><font face="Verdana" size="2">"It has taken some time for the people to adjust to
                    <img border="0" src="../../images/2002_october/AJ-sultan.jpg" align="right" hspace="4" vspace="4" width="150" height="220"> our prices and comfort," concedes Aqeel Jawad Sultan, Managing Director, JSE. The price of all the tickets at Al Shatti Plaza is RO2.500 (US$6.50), the highest in the country. Even well-to-do customers seemingly winced at the cost of watching a film at Al Shatti Plaza shortly after the new cinema hall opened in August 2000.&nbsp;<br>
                    <br>
                    This negativity has seemingly worn off mainly due to the increase in the disposable income of the Omani population, and also due to the fact that films are being shown as new as those shown elsewhere in the region. "Since January, we've getting films at the same time as in Dubai. Last year, we were getting films four to six weeks later," he explains. For instance, Pearl Harbour, was shown in Omani cinema halls several months later than Dubai.&nbsp;<br>
                    <br>
                    Another reason for the rising number of customers to Al Shatti Plaza this year is the fact that exclusive films are being shown. There are seven main distributors in Dubai that hold the rights to distribute major Hollywood films across the Gulf region. Currently, JSE has contracts to distribute movies from Twentieth Century Fox, Columbia, and Disney exclusively in Oman. Some of these agreements run until 2004. "We have an exclusive local distribution contract, hence a movie is not shown by any other cinemas when we are showing it," explains Sultan.<br>
  Ruwi Cinema's General Manager Raj Hingorani says another major reason why business has been steadily improving in 2002 is due to the increasing number of films that have been produced this year. The second half of 2002 has seen strong releases for Ruwi Cinema such as Scooby Doo, The Sum of all Fears and The Bourne Identity. During the past few weeks, Ruwi Cinema has also been screening Arabic movies such as Sehar Al Ayoon as well as Hindi movies such as Devdas.&nbsp;<br>
                    <br>
                    <img border="0" src="../../images/2002_october/cinma2.jpg" align="left" hspace="5" vspace="5" width="132" height="250">For Al Shatti Plaza, it was Spiderman that broke local box-office records. There are plans afoot to re-screen the film. Earlier in the year, Star Wars - Episode Two and Minority Report did very well. Currently, the movie XXX is breaking records. "Sixty per cent of what Spiderman earned in 20 days, XXX has generated in four days. Both films have been shown at Al Shatti Plaza exclusively," says Sultan. In March, Bollywood smash-hit blockbuster Kabhi Kushi Kabhie Ghum broke box office records at Al Nisr cinema.<br>
                    <br>
                    <b>Coming soon</b><br>
                    Meanwhile, Ramlat Al Ghafa LL, the company that has been running Ruwi Cinema since 1998, is currently busy preparing to open a new two-screen Bahja Cineplex next month in the basement of the Markaz Al Bahja shopping mall. The cinema will cost about RO3 million (US$7.8 million) to develop.<br>
                    "Muscat has got a huge shortage of entertainment, especially for young adults. We've tried to plug that gap," says Alan Fobian, General Manager of Markaz Al Bahja. Jawad Sultan Enterprises was initially approached to jointly invest in the Cineplex by the Ajay Group (the mall's owners) but the deal didn't go through as both the parties failed to agree on financial terms. Instead, Ramlat Al Ghafa took up the offer and signed a contract to initially operate the cinema inside the shopping centre for three years. Hingorani insists that the terms that have been agreed are workable although he is unwilling to announce how much the admission prices will be. "We have made our calculations," he says emphasising the vast number of nearby residents from Barka to Seeb Airport, whom he intends to target.<br>
                    In Oman, the first week of a film is obviously the most important week for cinemas which screen the film daily as many as four times. Films that do very well can survive on the main screen for more than five weeks. Those that flop are taken off in a matter of days. Every week, the cinemas show a new movie by popular demand to ensure they are not perceived as stale.<br>
                    <br>
                    <b>Facelift needed</b><br>
                    The old local cinema outlets have continued to feel the pinch, and a failure of a few to make improvements towards interior comfort has reduced customers substantially. "People choose those (halls) that are properly maintained, staffed and show more current films," explains Hingorani.&nbsp;<br>
                    Analysts say that older cinemas only get packed at weekends or during high tourist seasons. As a result, no more multi-million cinema projects are expected in Muscat for the time being. Nevertheless, Aqeel Jawad Sultan doesn't dismiss the idea of expansion in the capital area by his own company. He is confident that JSE has the infrastructure, finances and logistical expertise to keep ahead of any future threats should they arise.&nbsp;<br>
                    <br>
                    <b>New horizons</b><br>
                    Industry analysts believe that a new multi-screen cinema hall in Muscat City Centre could conceivably be readied quickly in future phases of this retail development.<br>
                    Expansion elsewhere in Oman is also a strong probability. Jawad Sultan Enterprises has plans to refurbish its existing cinema in Sur. Ramlat Al Ghafa used to run an open air cinema in Sohar which changed management in April and it's possible that there is room for another new cinema to exist in this location. In Salalah there are four cinemas, the Shanfari Group of Companies owns the best of these.&nbsp;<br>
                    Despite healthy subscriptions in satellite television and increasing ownership of DVD players in the Sultanate, local cinemas have continued to do well. In some way, this does not affect JSE's operations because it is the largest distributor of DVDs and videos in the local market. However, challenges may exist from pirated DVDs and VCDs, and cinema owners say that this poses little threat to the local industry.&nbsp;</font></p>
