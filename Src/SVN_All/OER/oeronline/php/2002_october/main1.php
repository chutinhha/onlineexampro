                  <td width="100%"><font face="Verdana" size="4" color="#FF0000">A cautious banker</font><p><font face="Verdana" size="1"><b>Stephen Davies, the man on the hot seat at NBO, is attracting a lot of media attention these days. But will he be applying strong-arm tactics to steer the bank back to profits or adopt a wait and watch approach, Saleh Al Shaibany finds out&nbsp;</b></font><font face="Verdana" size="2"><br>
                    <br>
                    <img border="0" src="../../images/2002_october/Nbo-gm.jpg" align="left" hspace="5" vspace="5" width="200" height="256"></font><font face="Verdana" size="2">After a flamboyant Aubyn Hill recently called it quits at the National Bank of Oman (NBO) as its top boss, all eyes are on Stephen Davies, the new General Manager and acting Chief Executive Officer. There are a few questions on everybody's lips. Will Davies run the beleaguered bank with an iron fist like his predecessor, or tread cautiously? Will he be able to inspire the board that he can lead the bank single-handedly or settle for a co-pilot seat? In any case, he is aware of the heavy burden on his shoulders and the expectations of the bank's investors.&nbsp;<br>
                    <br>
                    <b>Transparent banking</b><br>
                    After NBO recorded a loss (of RO6.4 million) in 2001, and considering that Hill had a controversial tenure at the bank, the new chief is conscious that the 2002 financial result may either make or break him. His refusal to give a peek at what strategy he has in store for the bank suggests that he is a cautious chief executive and would like to follow the wait and watch policy. Is this a hint that the board is firmly in control, at least for now, and not him? Anyways, it seems he is learning to walk the tight rope. For instance, he has pledged to continue hosting quarterly meetings to aid transparency.&nbsp;<br>
                    "It is a kind of mechanism that we will continue because it is important for brokers, analysts and journalists," Davies said in an exclusive interview with OER, his first interaction with media after he joined the bank three months ago. But Davies was guarded and spoke less. "Obviously, we cannot disclose many things today. But in the past, we have revealed a fair amount of information that was published in our reports," he rued.&nbsp;<br>
                    Some analysts say that this explanation is too vague for cautious investors. They argue that there is only one way to be transparent and that is to go all the way or not doing it at all. Although Davies promises he will be there in October to face questions, uncertainty still prevails whether he would be able to face up to intense interrogations or be in complete command.<br>
                    Many allege that Hill was trying to use the quarterly press meeting to promote himself rather than responding to a wider disclosure call. Considering this, Davies is unlikely to relish the prospects of facing investors and journalists who increasingly feel that they were taken for a ride by the smooth talk of his predecessor.&nbsp;<br>
                    <br>
                    <b>Low on lending&nbsp;</b><br>
                    In any case, there is a clear hint that he will attempt to make changes at some stage, but this will depend on whether he is confirmed as the CEO. "Change is important," he insists, "but you shouldn't change for the sake of change. Clearly, the bank has a very strong domestic franchise. We will try to reposition corporate banking a little bit."&nbsp;<br>
                    He however indicated in which direction the bank is likely to move. "We want to be selective and serve good quality customers. We want to make returns commensurate with the risks," says Davies. This means the bank is likely to tone down its aggressive strategy, and depart from what Hill had pursued. Hill's reckless expansion spree had burned the bank's fingers. Davies says, "We will be very selective on lending because of the associated risks, and focus more on retail business like deposit, which has no risks."<br>
                    <br>
                    <b>To push retail business</b><br>
                    However, Davies said that NBO would continue to "introduce and develop products" in the retail business and downsize overseas corporate lending. In fact, the bank's experience in overseas corporate banking, especially in Abu Dhabi and Egypt, had been perceived as not well-thought out. "We are downsizing our corporate business in Abu Dhabi and Egypt while putting more efforts on retail business in these countries. It is very difficult to be a significant player in a highly competitive market," he reveals. But he refuses to say how much NBO is planning to downsize in Egypt, or even in Abu Dhabi. A surprise announcement may come soon.<br>
                    NBO, however, will retain its presence in its current overseas locations. "We have been in Egypt for over 20 years. It is a significant market in the Arab region and we can successfully continue with our retail business there from our five branches, particularly in savings and deposit products. But we will cut our corporate lending," he says. The bank is also planning to expand its retail presence in the UAE, especially to emirates like Dubai. But NBO rules out a broad-based overseas expansion due to continued worries about the global economy.<br>
                    <br>
                    <b>Mum on merger</b><br>
                    Davies neither denies nor confirms that any merger is on cards. "I realise that there are rumours about NBO merging with another bank. Only board will decide that. Banks will always have to review their strategies, and I don't think we can rule out anything," he remarks.<br>
                    Focus on shareholder returns<br>
                    Davies, however, throws light on how the bank is planning to move ahead after the heavy loss in 2001. "What we got to do at this stage is to manage returns as best as we can in the current economic circumstances so that we deliver good value to our shareholders. Sometimes there are some aspects of the markets which you cannot control. For instance, nobody expected Nasdaq and Dow Jones to collapse the way they did two years ago," he says.<br>
                    Davies says he is satisfied with the bank's current returns. "Given the interest rate environment, we have a pretty good spread over," NBO is also planning to cut corners to improve the bottom line. What about profitability? The NBO chief refused to comment on the overall 2002 profits saying, "Obviously I am not at liberty to discuss our profitability. You have to understand that the markets in which we operate in are tricky and we will continue to focus on quarterly results." Are we really going to see NBO going back to the black in 2002? Only time can tell.&nbsp;</font></p>
