                  <td width="100%"><font face="Verdana" size="4" color="#FF0000">Take five!</font>
                    <p><font face="Verdana" size="1"><b>His Majesty's Cup is awarded to the five most industrious companies in Oman OER looks at why these companies won the trophy, what winning the cup has meant to them and how they expect to collect it again in 2000 by Nayl D'Souza</b></font><font face="Verdana" size="2"><br>
                    <br>
                    <img border="0" src="../../images/2000_march-april/comp_1.jpg" align="left" hspace="5" vspace="5" width="170" height="197">
                    According to the Ministry of Commerce and Industry (MCI) Oman Cement Company, Raysut Cement Company, Oman Flour Mills, Jotun Paints Company and Oman Filters Industry Company are the crème de la crème of their respective industries in the Sultanate.&nbsp;<br>
                    <br>
                    The prestigious His Majesty's Cup award, first initiated by the MCI in 1991, signifies the success of these private sector firms in achieving ministry targets and raises their international profiles.&nbsp;<br>
                    <br>
                    "The criteria set for this competition includes the achievement of high Omanisation rates, value addition, increase in exports and commitment to quality and specifications regarding occupational safety," said Minister of Commerce and Industry Maqbool bin Ali Sultan.&nbsp;<br>
                    <br>
                    The MCI also commends a firm's use of local raw materials, overall quality of product, marketing capability and protection of the local environment.&nbsp;<br>
                    However, the main criterion of the competition is Omanisation, which takes into account not just the number of Omanis in an organisation but their representation at management level and involvement in training programmes.&nbsp;<br>
                    <br>
                    Maqbool bin Ali Sultan says such contests improve productivity and the efficiency of companies in turn raises the overall industrial sector's contribution to Oman's gross domestic product (GDP). Moreover, Maqbool highlighted the support and encouragement given by the private sector to the ministry's plans for the improved development of higher quality Omani products and services.&nbsp;<br>
                    <br>
                    <b>Jotun Paints Company LLC<br>
                    </b><br>
                    Jotun Paints Company LLC (JPC) is essentially a Norwegian corporate that concentrates 14% of its operations in the Middle East, 18% in the Far East and 68% in Europe. It has 35 factories and operations in 53 countries.<br>
                    <br>
                    Since its Omani inception in 1985, JPC has increased its distribution all over the Sultanate and achieved a 45% market share in paints for decorative, marine and industrial use. Indeed, the company has realised total operational growth of over 30% over last three years. Moreover, JPC has forged considerable growth in exports with a rise from 6.0% of production in 1997 to 15% in 1999. Additionally, the company plans to expand to Yemen, Tanzania and Kenya.<br>
                    <br>
                    Regarding Omanisation, we have 41% and we have a concrete plan to have 50% by 2003," says JPC's general manager Vijay Kumar.&nbsp;<br>
                    <br>
                    "At Jotun, we have a long term strategy and concrete plan put it into practice on Omanisation. Our policy means that Omanis are not simply employed on the shop floor for show. We have representations in senior management, middle management, executive level as well as staff and operational levels," adds Kumar.<br>
                    <br>
                    Apart from creating employment opportunities for the local Omani workforce, JPC actively involves the community by seeking the use of local contractors. "Having approved formulations in our regional laboratories, we currently use calcium carbonate from a local manufacturer and also a local canning factory too. We use the local community wherever possible. It benefits us in terms of speed of supply and obviously benefits the domestic economy by boosting local revenues," he adds.&nbsp;<br>
                    <br>
                    Self-proclaimed as a 'friend to society and companion for the community', JPC is environmentally conscious having already introduced eco-friendly water-based production techniques. "Adopting the latest technology, we plough back 2.0% - 3.0% of investment on research development. We have over 350 chemical engineers in Norway working on new products with a focus on environmental friendly marine paints and protective coatings," adds Kumar.<br>
                    <br>
                    JPC claims to be first liquid paint company to get ISO 9001 certification in the entire Middle East after Saudi Arabia. In accordance with their mission statement, Vijay Kumar suggests that JPC customers choose them because of the quality of products, personnel and service, adding: "We want Jotun Paints perceived as the most professional paints supplier in this country on the basis of our quality. We have a quality awareness and process which assures standards are maintained and monitor such standards rigorously."&nbsp;<br>
                    <br>
                    JPC, with a capital investment of RO1.8 million, saw sales of RO4.4 million in 1999. It also achieved a net profit of RO827,000 and increased its annual operating profits on a yearly basis over its 14 years of operation.<br>
                    <br>
                    <b>Oman Filters Industry Company SAOG<br>
                    </b><br>
                    Oman Filters Industry Company SAOG (OFIC) has achieved significant improvements in Omanisation, safety and quality since it was established in 1995.<br>
                    <br>
                    The company has achieved a 40% level of Omanisation and saw sales turnover of RO433,000 in 1999. Although the company did not break into profit in 1999 with regard initial capital expenditure and sales, OFIC achieved improvement in turnover and the company, a 100% Omani joint stock firm, has a capital expenditure of RO2.98 million.&nbsp;<br>
                    <br>
                    Regarding its export markets OFIC General Manager Mohammed Sultan Sheibany said the company has developed markets in GCC countries, Lebanon and Egypt. "It is a recognition of our achievement and we are proud of it," he added.&nbsp;<br>
                    <br>
                    <b>Oman Flour Mills Company SAOG<br>
                    </b><br>
                    Oman Flour Mills Company SAOG (OFMC) is a joint stock company owned 60% by the government of Oman and 40% by Omani public shareholders. Established in 1976, the company started commercial production a year later and has been the undisputed market leader in the Sultanate's industrial food sector ever since.<br>
                    <br>
                    OFMC attained Omanisation of 76% in 1999 and Omanis currently represent 77% of the total workforce.&nbsp;<br>
                    <br>
                    Despite new competition from Salalah Flour Mills Co. SAOG, a recent private sector entrant in the domestic food industry, OFMC is continuing to grow its success. OFMC produces wheat flours, speciality flours, cake mixes and different types of animal feed. The company owns and operates a 120,000 metric ton (MT) grain silo, an 800 tons-per-day (tpd) flour mill and 600 tpd animal feed mill. Moreover, the manufacturer produces smaller lines of production that contribute a further output of 250 tpd and own a 54% shareholding in a poultry farm.&nbsp;<br>
                    <br>
                    OFMC has a 40% export percentage and has plans to expand in African, Russian, GCC and Far East markets. It has also made improvements in food security which are major factors in harmony with the MCI's vision of industrial excellence.<br>
                    <br>
                    <b>Oman Cement Company SAOG<br>
                    </b><br>
                    Oman Cement Company SAOG (OCC) was established by the MCI in 1977 and the company leads the domestic cement industry. OCC is the only manufacturer in north Oman and has expanded capacity to 1.25 million tons-per-annum to supply the 1.2 tons-per-annum northern Oman market.<br>
                    <br>
                    One important criteria for winning HM's Cup is the use of local raw materials and OCC uses 90% of its supply from limestone and silica deposits close to the plant. Furthermore, OCC has a sophisticated manufacturing process using the latest technology which has seen it achieve ISO 9002:1994 certification.<br>
                    <br>
                    OCC expanded its cement production capacity to 1.25 million tons-per-annum in 1998. The company has also commissioned a 30 megawatt captive power plant at its site in Rusayl Industrial Estate which will ensure uninterrupted power supply to the plant and reduce costs.&nbsp;<br>
                    <br>
                    In Oman, the major drivers are the government and housing sector which each consume approximately 40% of cement production. Private sector projects account for the remaining 20%.&nbsp;<br>
                    <br>
                    The equity capital of OCC was increased to RO33 million in a public subscription in 1994, after which shares were listed on the Muscat Securities Market. The government holds a 69% stake in the company, of which 43% is held through the MCI. The company's cash position is good with close to RO23.5 million forming an investment and deposits portfolio.<br>
                    <br>
                    One leading market analyst says: "Private sector projects are likely to boost the cement sector demand in the coming years. OCC appears to be a good company with quality, responsive and contemporary management."<br>
                    <br>
                    <b>Raysut Cement Company SAOG<br>
                    </b><br>
                    Raysut Cement Company SAOG (RCC), OCC's domestic rival, produces cement for the relatively smaller southern Oman area where demand is approximately 0.2 tons-per-annum. And while OCC competes in the UAE market, RCC has chosen to explore export market potential in Kuwait, Somalia, Sri Lanka and Mauritius and reduce sales to less favourable markets in Yemen and Kuwait.<br>
                    <br>
                    To facilitate sales in North Oman, the company is to set up a distribution terminal in Muscat at a total cost of RO0.8 million. The terminal, which is expected to begin operations in May, will save shipping costs and improve customer response time.&nbsp;<br>
                    <br>
                    RCC was formed in 1981 and started its operations in 1984. The company owns all its raw material quarries. In the Sultanate, RCC has about a 30% market share and a capital investment of RO 36 million.&nbsp;<br>
                    <br>
                    <br>
                    <b>What does Winning the Cup mean?</b><br>
                    <br>
                    Winning HM's Cup is a great honour. It is a reward recognising the efforts of Oman's vibrant industry. Winners cherish the raised profile they receive and are proud to attain levels of organisational success both economically and socially.&nbsp;<br>
                    <br>
                    Are there any additional benefits from winning the award beyond the prestigious accolades and excellent publicity?&nbsp;<br>
                    <br>
                    The call of industry is for more support through improved assistance by visible subsidies as well as more stringent checks and severe punishments for sub-standard imports that do not meet the Sultanate's safety and quality specifications. However, the World Trade Organisation will clearly deter member countries from taking direct unilateral action through the use of import restrictions.<br>
                    <br>
                    <b>Future Success</b><br>
                    <br>
                    All the winners of HM's Cup have mentioned definite plans to win again in 2000. They will continue to monitor Omanisation, customer needs, occupational safety and product quality. Accordingly, improvements will again be made in all these parameters as well as operational excellence and professionalism.&nbsp;<br>
                    <br>
                    "We will be keeping the best local and expatriate workforce by offering incentives and training. Our goal is to head hunt local manpower and select the best available employees so that we remain competitive," says another industry official.</font></p>
