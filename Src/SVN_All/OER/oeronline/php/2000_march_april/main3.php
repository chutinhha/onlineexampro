                  <td width="100%"><font face="Verdana" size="4" color="#FF0000">Pleasing outlook for leasing</font><p><font face="Verdana" size="1"><b>Leasing and hire purchase companies, popularly known as specialised finance firms, are poised to leap into a new era of potential profitability in view of the mandatory requirement to raise capital during the current year. OER reports</b></font><font face="Verdana" size="2"><br>
                    </font><font face="Verdana" size="2">
                    <br>
                    </font><font face="Verdana" size="2">Availability of funds and thereby the financial strength of these firms is expected to increase further as equity capital will be augmented to the stipulated level of RO5 million.<br>
                    <br>
                    Of the five finance companies in operation where barring United Finance, the paid up capital of all other firms is less than the stipulated level and they will have to find alternative ways to raise equity. In some cases, the shortage is marginal, but in others the difference is substantial. For example, Al Omaniya Financial Services has a shortfall of RO2 million, while Muscat Finance has to augment capital by merely RO118.000.<br>
                    <br>
                    Over the last couple of years, there has been growth in the availability of funds from finance companies as two new firms- United Finance and Al Omaniya Financial Services - entered the market. Now, two more business groups are planning to set up separate leasing and hire purchase companies, thereby taking the total number of specialised finance companies to seven.&nbsp;<br>
                    <br>
                    </font><font face="Verdana" size="2">Aftab Patel, general manager of Al Omaniya Financial Services, says his compnay is planning to mop up RO2 million by way of a rights issue, subject to necessary approvals from the regulating authorities. The company's paid up capital is currently RO3 million, which will be raised to RO5 million after the rights issue. However, the boards of other companies are yet to take a decision on the ways to raise equity to the required level.<br>
                    <br>
                    For funding requirements, leasing and hire purchase companies depend mainly on equity capital, short-term borrowings from banks and credit from local and international banks. As the banking law provides for foreign currency exposure up to 40% of the finance company's net worth. Beyond that limit, these institutions need to get approval from the Central Bank of Oman which clears such requests on a case-to-case basis. In the recent past, these firms have sought permission to raise deposits from public investment in order to finance their growth in the future.&nbsp;<br>
                    <br>
                    </font><img border="0" src="../../images/2000_march-april/finan_1.jpg" align="left" hspace="5" vspace="5" width="142" height="180"><font face="Verdana" size="2">Elaborating on the recent developments in the financial services field, Raphel Parambi, chief executive officer of the Muscat Finance Company (MFC), said that the market has grown quite well and now the challenge is to serve the people in the interior where the majority of developments are. "There is a need to develop the market for equipment leasing, especially for small businesses, in the interior region. This is important as these small companies provide a lot of employment and entrepreneurial opportunities for Omanis."<br>
                    <br>
                    Analysts say that the increase in oil prices and the roll back in customs duty will have a positive impact on the revival of the economy. "There will be overall economic growth and that in turn will be reflected in the retail market," Parambi added. He believes that while the economy is expected to strengthen there will be general decline in bank interest rates.&nbsp;<br>
                    As government expenditure is seen growing by 13% by the end of 2000 according to the budget estimate, there will be tremendous activity in the economy and that in turn will benefit the leasing and hire purchase companies.<br>
                    <br>
                    Referring to the economic recovery and the business environment in 1999, United Finance Company Chairman Hamed Said Al Mahrouki said that the rise in oil prices in the second half of the year, the reorganisation of capital market, the announcement of 14 industrial projects by Ociped and start of work on the breakwater project at Sohar has buoyed up the market.&nbsp;<br>
                    <br>
                    "The bite of the 1998 and early 1999 recession was so severe that these positive features did not encourage enough activity to mark an economic upsurge and we saw a process of liquidation, dissolution and merger of some large businesses which could not withstand," noted Al Mahrouki in the company's annual report.&nbsp;<br>
                    <br>
                    The Central Bank of Oman continued to monitor the banking environment in the country and its actions in controlling consumer credit and limiting banks' overseas resourcing had a positive effect on the area of United Finance's operation. Interest rates continued to rule high, with levels being as much as 2% over corresponding global and regional market trends.<br>
                    <br>
                    It is generally expected that for 2000, liquidity will be low and that may cause delays in repayments. Therefore, finance companies will have to be little more cautious. "There will be a positive impact on demand for funds in the long run, but there will be an adverse development as far as the repayment is concerned in the short term," according to a senior official at one hire purchase company.&nbsp;<br>
                    <br>
                    With the growth of private sector and other opportunities, finance companies are exploring the possibility of further extending their services to meet the working capital and capital expenditure requirements of small companies.&nbsp;<br>
                    <br>
                    "With several new infrastructural development, industry and tourism projects planned, companies are looking ahead with hope and confidence. The board (of United Finance) has directed the management to study the market beyond Oman for possible future expansion of its horizon," noted Al Mahrouki.&nbsp;<br>
                    <br>
                    Leasing and hire purchase companies are likely to face competition due to the plans of some business groups to diversify into this field by floating separate firms.&nbsp;<br>
                    <br>
                    Aftab Patel says that companies in Oman have sufficient strength and management back up to withstand any competition. "Since the established local companies have the advantage of knowledge about the customers, they will be in an advantageous position. Customers will select a company that is comfortable for them to deal. Further, since there is easy access to technology now-a-days, a kind of level playing field exists among the players, irrespective of the size of the company," Patel added.&nbsp;<br>
                    <br>
                    Parambi feels that there is only a limited overlap in the businesses of commercial banks and specialised finance companies. "There is an overlap between commercial banks' personal loan and corporate finance schemes and specialised finance companies' retail finance and lease finance schemes. But this over lap is minimal," he says. When business groups are planning to diversify into leasing and hire purchase in the domestic market, MFC in association with other local partners started spreading its wings to overseas markets. The company, in association with three Omani financial institutions and other investors, has taken over the Bangladesh-based Bahrain Bangladesh Finance &amp; Investments (BBFI) and a new management with the reconstituted board was formed. The Omani partners have a combined equity stake of 70.58% in the firm that is to be renamed as Oman Bangladesh Leasing &amp; Finance Company. With two branches, BBFI will continue its core business of leasing and hire purchase in Bangladesh, while factoring will be introduced at a later stage. The company, a closely held firm promoted by a group of investors from Bangladesh and Bahrain, has been in operation for the past two years.&nbsp;<br>
                    <br>
                    MFC has also sought CBO permission for issuing debt instruments such as convertible bonds and certificates of deposits (CDs). In the recent past, the company has introduced many innovative schemes for the domestic customers. For example, in addition to launching factoring, the company has joined forces with four local companies to offer the 'Star Select' package of pay-to-view television channels to residents of Muscat on an installment basis. As per the agreement, the Star Select package will be available as part of MFC's home plan. The instalment payment scheme aims at reducing the initial burden of investment associated with acquiring pay-to-view television channels. Further, the company is evaluating finance schemes for fishermen.&nbsp;<br>
                    <br>
                    Al Omaniya Financial Services, another leading firm, has emerged as a significant player in the Sultanate's asset financing and leasing business in a short span of two years. Referring to the new products in the pipeline, Patel said that the company is planning to launch consumer durable finance scheme shortly. In addition, it is looking at opportunities to open a couple of branches to better serve the customers. "The retail car finance is our core business and the company is strong in that area. In addition, we concentrate on lease finance to transport companies and corporate loans to business enterprises", he noted. The company offers financial products specially designed to suit the needs of each and every segment. "For instance, we have a special automobile finance scheme where the borrower need not have to pay during the period of Eid", he said.&nbsp;<br>
                    <br>
                    In another development, the United Finance Company (UFC) has received approval for debt factoring and financing of receivables. "This product will assist us in increased servicing of small and medium enterprises. Though the company is new, we have created a niche market and are on level playing field with major players", Al Mahrouki stated. Previously, Muscat Finance was the only player providing factoring service in the Sultanate. These new products are expected to augment the income stream of the company. UFC is the second firm that received such licence. The company was formed with the aim of providing secondary source of funds for corporate sector, after traditional primary role of commercial banks in this sector.&nbsp;<br>
                    <br>
                    As far as the historical development of specialised finance companies is concerned, keeping in line with the government policy on developing a vibrant private sector, these firms were formed over the past two decades. Starting its operations as an automobile finance firm in 1987, MFC is now represented in all major centres of the Sultanate. The company has six branches - Muscat, Sohar, Barka, Ibri, Salalah and Sur. Apart from auto finance, the company specialises in equipment leasing and non-vehicular asset financing. Major financial institutions like Ominvest, Oman International Bank (OIB) and OIFC have jointly promoted MFC.&nbsp;<br>
                    <br>
                    Oman Leasing was formed subsequently, while Oryx Leasing was set up a little later as the third leasing company. Subsequently, United Finance Company and Al Omaniya were formed in the nineties. Earlier, three commercial banks - Citibank, Bank Muscat and National Bank of Oman - were engaged in automobile finance. However, over a period of time, these banks realised that it was not fitting in their long-term strategy and therefore, withdrawn from the area. In 1997, Al Omaniya Financial Services entered the market with its own name and bought the automobile finance business of the Bank Muscat for RO8 million.</font></p>
