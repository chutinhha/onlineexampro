                  <td width="100%"><font face="Verdana" size="4" color="#FF0000">Canned success</font><p><font face="Verdana" size="1"><b>A view inside the National Cans Packing Industry factory<br>
                    The opening in December 1999 of Oman's first can manufacturing factory has given a big boost to the local food processing industry that was dependent on imported cans to pack its products - by Lawrence Pinto</b></font><font face="Verdana" size="2"><br>
                    </font><font face="Verdana" size="2">
                    <br>
                    <img border="0" src="../../images/2000_march-april/comp_3.jpg" align="left" hspace="5" vspace="5" width="265" height="166">The National Cans Packing Industry (NCPI), promoted by Al-Haditha Group of Companies, is only the third manufacturing facility of its kind in the Gulf region. The RO4.2 million factory at Rusayl Industrial Estate is designed to produce OTS (open top sanitary) cans and general line aerosol containers as well as rectangular cans and twist-off caps for a range of industries including food processing, paints, lubricants and insecticides.<br>
                    <br>
                    The factory is fitted with the latest can processing and welding machinery and equipment imported from Switzerland, Germany, Japan and Italy. The two colour offset sheet metal printing machine, imported from the Far East, complements the plant further.<br>
                    <br>
                    The factory has five production lines to produce a variety of metal cans ranging from 70 grams to 20 litres for packing processed food products, tomato paste, baking powder, fruits, vegetables, mushrooms, fish, milk powder, edible oil, lubricants, paints, aerosol cans for insecticides and air-fresheners and twist-off caps for ketchup bottles.<br>
                    <br>
                    The production capacity of the plant varies depending on the size of the cans. The high-speed line for 70 grams tomato paste cans has capacity of over 700 cans per minute. The intermediate lines, for producing 850 grams cans for tomato paste, mushrooms, corn flower, coffee powder and one litre tins for paints etc., have a capacity of 300-400 pieces per minute while the 20 litre paint tins line produces about 20 cans per minute.<br>
                    <br>
                    Murtadha Jaffer Rehmatullah Al Lawati, chairman and managing director of the company, says that the project is of national importance because it will encourage farmers to grow more and export. "While helping local consumption and steering the country towards food self-sufficiency, it will save and generate foreign exchange," he says.<br>
                    The NCPI has a professional management team, which is headed by group general manager Mohammed Salahuddin. He points out that six years of preparation has gone into the project. Results of a feasibility study commissioned by the group although showed that there was a substantial demand for cans in Oman, the various food product and paint manufacturers had to rely on imports.<br>
                    <br>
                    "The availability of cans locally should now encourage more food processing industries, particularly in the perishable items like fish, vegetables, fruits etc," says Salahuddin.<br>
                    <br>
                    Once the project's feasibility was ascertained, a team was dispatched to Europe, Singapore and Malaysia to identify the technology, machinery and equipment best suited for setting up the can manufacturing unit.<br>
                    <br>
                    Oman's food product exports have seen steady increase in recent years, rising from RO48.1 million in 1996 to RO52.9 million in 1997 and to RO59.1 million in 1998. Previously, Oman's requirement for cans was met by imports from Italy, Poland, Singapore, Malaysia, Australia and Saudi Arabia. The NCPI is now working towards meeting 100% import substitution in due course.<br>
                    <br>
                    "The plant incorporates three processes - making of ends, both top and bottom, and the body itself as well as printing as per customers' requirement," says Afroz Hadi, manager - sales and marketing.<br>
                    <br>
                    Hadi travelled extensively within Europe and Southeast Asia to identify the right can manufacturing processes and machinery. He not only visited the machinery manufacturers' plants but also went around and saw how the machines were performing before deciding a particular make or brand.<br>
                    <br>
                    He says that the NCPI has a fully integrated cans manufacturing facility for OTS as well as general line food processing cans. The factory is well equipped not only to meet the entire domestic demand but also export about 70% of its total production.&nbsp;<br>
                    <br>
                    Since beginning commercial operations last November, NCPI has been supplying cans to several local companies. These include Oman Agro Industries, which produces Jumbo brand tomato paste and Naeem brand canned vegetables, fruits and fruit juices and Oman Foodstuff Factory, which produces Al Mudhish brand milk powder, tomato paste and coffee powder.<br>
                    <br>
                    Aerosol containers are to be supplied to Oman Chemicals Company, which manufactures Flee Flea brand insecticides, and Presto brand air-fresheners and Oman Insecticides &amp; Air-freshener Company, which produces Tox and Toxplus insecticides and Ayam brand air-fresheners and deodorants.&nbsp;<br>
                    <br>
                    NCPI is already supplying to three of the four paint manufacturers in the country - Asian Paints, Khimji Permoglaze and Sadolin Paints. In addition, it is also undertaking the printing jobs for Areej Vegetable Oils and Derivatives that was earlier getting it done in the UAE or Far East.<br>
                    <br>
                    Hadi says the company has been supplying tomato paste cans to a couple of bulk fillers in the UAE, mainly in Abu Dhabi and Ajman. "There have been some positive enquiries from Kuwait. Saudi Arabia is a big market, which is yet to be tapped, followed by Yemen. We will be moving into these markets gradually. In the meanwhile, negotiations are on with Asian Paints for the supply of cans for their Indian as well as several overseas operations."<br>
                    <br>
                    Quality control and adherence to international hygienic standards are given foremost attention at NCPI. "In the food industry, quality of packing materials and the lacquers used for lining the cans are of utmost importance. Therefore, we source our main raw materials of tin plates and lacquers from the best suppliers in Europe and Japan."<br>
                    <br>
                    As part of its second phase expansion, NCPI intends to manufacture beverage and two piece fish cans, which has good potential. Since the process necessitates new equipment and involves additional investment, the company also has its options to go public in the near future.</font></p>
