<p align="left" dir="ltr"><font face="Verdana" size="4" color="#FF0000">
<br>
</font><font face="Verdana" size="6"><b>A parallel global capital market?<br>
</b>
</font><font face="Verdana" size="1"><br>
<b>OER columnist Matein Khalid describes the history of the emergence of Islamic 
financing as a US$400 billion market</b></font></p>

<p align="justify" dir="ltr"><b><font face="Verdana" style="font-size: 11pt">
<img border="1" src="periscope.jpg" align="left" width="95" height="118">T</font></b><font face="Verdana" size="1">he 
birth of Islamic banking lay in the geopolitical events of the 1970’s. The 
petrodollar bonanza of 1974, the emergence of Saudi Arabia as a financial 
superpower of the Middle East, the Iranian Revolution, the search for legitimacy 
by military regimes in countries such as Pakistan and Sudan by “Islamising” 
their societies, a new wave of Islamic consciousness among the intellectual 
elite of Southeast Asia. <br>
<br>
Yet, ironically, history demonstrates that the evolution and momentum of Islamic 
banking and finance was facilitated by free market forces and visionary 
entrepreneurs such as Prince Mohammed bin Faisal Al Saud and Sheikh Salah Al 
Kamel, and not government diktat! The embryonic industry’s early years were not 
without struggle — a wave of failures of Islamic banks and investment companies 
in Egypt and Pakistan, non-competitive product, a reputation as a home for 
mediocre bankers, skepticism by Western central bankers and regulators, and even 
links to banned charities after 9/11. Even now, the banking systems and savings 
pool of the Middle East is overwhelmingly conventional, not Islamic.<br>
<br>
With almost US$400 billion in assets, 275 specialist financial intermediaries, a 
spectrum of innovative financing techniques across all major asset classes and a 
presence in more than 75 countries, Islamic banking has evolved from an obscure 
niche to an integral component of the global financial village. While 
Shariah-compliant finance has had its greatest impact on the GCC/Arab financial 
markets and Muslim nations such as Pakistan, Malaysia and Iran, its global reach 
now reaches across the world financial markets and Main Street America.<br>
<b><br>
Global Reception</b><br>
Global banking colossi such as Citigroup, HSBC, UBS, ABN Amro and Lloyds have 
all created Islamic subsidiaries or windows to design Shariah-compliant products 
that range from home mortgages in the UK to structured finance, private equity, 
project finance and investment banking. The Sukuk has become a viable segment of 
the international debt markets, with US$6 billion in new issues from sovereign 
(Malaysia, Pakistan, Qatar), corporate (Emirates Airlines), supranational 
(Islamic Development Bank) and even non-Muslim (German state of Saxony-Anhalt) 
borrowers in the Euromarkets. <br>
<br>
The creation of the Dow Jones Islamic Index has led to a proliferation of 
global, regional and country funds, a ballast to the Islamic wealth management 
industry. Some of the most successful financial IPO’s in the Arab world-Bubayan 
Bank in Kuwait, Al Bilad Bank in Saudi Arabia, Amlak Finance in the UAE, QIB in 
Qatar — have all been Shariah-compliant institutions. Islamic investment banks 
have structured and placed capital markets products across asset classes as 
diverse as commodities, real estate, leasing, foreign exchange, trade finance, 
emerging markets debt, private equity and venture capital. <br>
<br>
Bahrain, Dubai, the Malaysian offshore financial center of Labuan and Luxembourg 
have consciously made Islamic finance a template of their strategy and value 
proposition. <br>
<br>
Most importantly, an entire generation of Arab and Muslim bankers have led the 
innovation wave that has enabled Shariah-compliant finance to transform the 
banking systems and capital markets of the MENA nations and Malaysia. Every 
major project finance deal in the GCC invariably includes an Islamic tranche. 
There is now even an Islamic hedge fund in existence, with a Shariah Board and 
its own black box quant trading model.<br>
<br>
The macro themes behind the resurgence of Islamic finance in the Middle East 
since the late 1990’s have converged. Soaring oil prices, repatriation of 
capital from the Western economies, telecom deregulation, demographics, the 
securitisation of finance, spread of real estate ownership, the regulatory 
incentives offered by offshore centers such as Bahrain and Dubai, the 
infrastructure finance boom, the emergence of local stock markets and flotation 
of new specialist Islamic banks, banking deregulation increased emphasis on fee 
income in banking, intra-Arab capital flows, the eagerness of global banks to 
lend Islamic finance their brand imprimatur to access a potential worldwide 
population of 1.2 billion Muslims, have all played their role in the brace new 
world of Islamic banking and finance. <br>
In fact, across MENA, Islamic institutions pace innovation in the capital 
markets, finance the regional entrepreneurial impulse, and lead the state’s new 
engagement with the private sector.<br>
<b><br>
Plain Vanilla Murabaha</b><br>
Islamic banking has evolved greatly from its roots in the plain vanilla murabaha 
(cost-plus finance) contracts of the 1970’s to include insurance (takaful), 
leasing, fund management, private equity and venture capital. <br>
<br>
A parallel capital markets universe now exists, derived from the ancient 
paradigm that linked finance, entrepreneurialism and ethics in the Red Sea 
caravan trade in the ancient Hijaz. Most of the landmark financials deals in 
MENA have had an Islamic dimension: Etisalat’s strategic purchases of Pakistan’s 
privatized telecom, the syndication deals behind the Dolphin Project (the 
biggest cross-border project in the GCC), the IPO of Al Bilad Bank (the newest 
bank IPO in the kingdom), the State of Qatar’s Eurosukuk new issue, NCB’s 
decision to convert all retail finance products into Shariah-compliant product 
and the UBS Noriba’s design and placement of the world’s first Islamic hedge 
fund.<br>
<br>
The growth metrics of Islamic finance are accelerating in the Gulf. Conventional 
banks from Kuwait to the UAE have “converted” to Islamic banks. Citi Islamic, 
the Bahrain subsidiary of the world’s largest bank and the symbol of Pax 
Americana in the Middle East, has US$6 billion in deposits. <br>
<br>
From consumer finance to sovereign borrowing to equities portfolio management, 
from trade finance to home mortgages to estate development from IPO finance to 
private equity, a sophisticated Islamic solution is often offered by banks and 
fund managers in the embryonic capital markets of MENA.<br>
<b><br>
Systematic Challenges</b><br>
Islamic banking and finance faces significant systemic challenges. The bulk of 
the sector’s assets are still invested din murahaba, quasi-money market 
instruments. There are often rivalries, undisclosed conflicts of interests and 
theological contradictions among the handful of Shariah scholars who dominate 
Islamic bank boardrooms. <br>
<br>
Product innovation is stunted by lack of global standards and audit treatment. 
Sukuk have no real secondary markets and are often de facto syndicated loans. 
Islamic banks are compelled to hold a huge proportion of illiquid, unprofitable 
money market assets, have no viable inter-bank market and no unambiguous access 
to central bank lender of the last resort window. Yet, Shariah-compliant finance 
is no fad but a seminal catalyst for nothing less than a banking revolution in 
the Middle East.</font></p>

