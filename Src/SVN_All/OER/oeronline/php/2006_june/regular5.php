    <p align="center" style="margin: 10" dir="ltr">
    <font face="Verdana" style="font-size: 16pt"><b>Expand fast or perish</b></font><p align="justify" style="margin: 10" dir="ltr">
    <font face="Verdana" size="1">Recently, Muscat had the privilege of hosting 
    the 8th Arab Telecom and Internet Forum. Over 500 high-level personalities 
    attended the Forum. The delegates included the Arab ministers of 
    communications, management heads of telecom companies and industry analysts. 
    The congregation of the policy makers and implementers from the Arab world 
    provided an excellent insight into the fast changing dynamics of the 
    telecommunications space in the region. <br>
    <br>
    As per a recent report from Arab Advisors Group encompassing over 18 fixed 
    voice operators and 39 cellular operators in 18 Arab countries, there were 
    85 million cellular subscribers and 30 million mainlines in 2005. It 
    indicates that in 2005, cellular operators added around 34 million 
    subscribers while the fixed line operators added 2.4 million mainlines. The 
    figures clearly show that the telecom market in the Arab world is expanding 
    at a very high rate. The Arab telecom markets are expected to sustain this 
    fast growth rate in the next few years. <br>
    <br>
    The fast pace of growth could be attributed, to a large extent, to the 
    integration of the Arab economies with the global market. Most of the Arab 
    countries are liberalising and expanding their telecommunications sector. 
    The state-owned monopolies are either giving way to new and aggressive 
    private operators, or transforming to the requirements of the changed market 
    requirements. <br>
    <br>
    At home, we have witnessed how Omantel and Oman Mobile are transforming, 
    coupled with the meteoric growth of mobile subscribers with the entry of 
    Nawras. Already, there are talks of the entry of additional service 
    providers in the mobile, fixed line and Internet services in the Sultanate.
    <br>
    <br>
    Similarly, the other Arab countries are also busy formulating policies to 
    further attract private investment in the sector. In the race to provide 
    more options to the telecom subscribers, the Arab governments are allowing 
    multiple operators to compete with each other, as currently there seems to 
    be space for more players in most of the markets. But the big question is: 
    are we planning to be ready for the next phase of competition?<br>
    <br>
    The Arab world telecom market is served by a number of small to medium size 
    telecom service providers. Despite leading in their home markets, many of 
    them lack the resources or expertise to make high investments in the new 
    technologies on a sustainable basis in the coming years. Many a time, the 
    feasibility - due to the small market size -could be a major concern for 
    them. <br>
    <br>
    For example, while 3G (Third Generation) Technology has been introduced by 
    some of the service providers, it may not fetch them adequate revenue to 
    justify the high investments, as there won’t be enough takers among the 
    subscribers initially. However, in order to offer matching services to beat 
    the competition, they may be forced to bleed.<br>
    <br>
    Secondly, the global trend is shifting towards single operator offering 
    multiple services, including across mobile, fixed and Internet etc. This 
    means that small to medium size companies may find it very difficult to 
    survive in the long run. The big companies - with their deep pockets and 
    large-scale operations - could well swallow them through merger and 
    acquisition routes. <br>
    <br>
    Even the big telecom firms from the non-Arab markets, which were not very 
    serious about the Arab world earlier, are showing deeper interest to make 
    inroads into one of the fastest growing telecom markets across the globe. 
    They are forming joint ventures to bid for new licenses to gain a foothold 
    before flexing their muscles later on. <br>
    <br>
    Perhaps, the solution lies in forming cross-border alliances and 
    partnerships in the Arab world to share resources and create the critical 
    mass to survive the big fight. The more dynamic companies from the 
    comparatively smaller markets must change gears and cross over the borders 
    to build pan regional business operations more rapidly. <br>
    <br>
    This may not be as simple as it may sound. But they need to take steps today 
    to be ready as the market moves from the current phase of expansion to 
    consolidation tomorrow. <br>
