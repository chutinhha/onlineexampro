<b><img border="1" src="spotlight5.jpg" align="right" width="150" height="143"></b>Imagine yourself stranded in deserted area because your car battery has given 
up? A battery problem is probably the last thing one can dread off while 
driving. Though not much is required to do when it comes to battery care if not 
properly handled or taken care of, car batteries can cause serious injuries and 
damages. Batteries require simple and
routine maintenance in order to work properly over their lives. <br>
<b><br>
Battery HygiEne</b><br>
Keep the top of the battery clean. Dirt becomes a conductor, which drains 
battery power. Further, as corrosion accumulates on battery terminals it becomes 
an insulator, inhibiting current flow. It is advisable to check the condition 
and charge of the battery. Always replace the battery with one that has a higher 
rating than the original. Dispose your car battery properly. Car batteries are 
highly recyclable items with their plastic and lead components. Batteries also 
contain large amounts of harmful lead and acid solution which call for proper 
disposal in order to protect our environment. <b><br>
<br>
Battery Safety</b><br>
Charging a battery releases hydrogen — this gas is explosive and the slightest 
spark can cause the battery to explode, Therefore give the right mixture of gas 
and air. Ventilate it well. The sulphuric acid of the battery can severely 
injure eyes and skin. Never tip or drain a battery. Batteries are heavy always 
be careful while lifting or replacing. <br>
<br>
Do not jumpstart a vehicle by connecting its battery with a different voltage 
battery from another car. There were also occasions where fires and explosions 
were caused due to short-circuiting of the batteries. Start the car with the 
good battery and let it continue to run at high idle for a while to allow the 
dead battery to be recharged and to warm its electrolyte. Check and make sure 
that the “terminals” of your car battery are protected with insulating caps. 
This is to protect your car battery from short-circuiting. Moreover, to ensure 
that your battery lasts long, make sure the electrical system is charging at the 
correct rate; overcharging can damage a battery as quickly as undercharging. If 
your battery is the type that needs to be topped off, check it regularly, 
especially in hot weather. Add distilled water when necessary. <br>
Tips to keep your car battery going If you get behind the wheel only to discover 
that your car’s battery has given up you may be able to get your vehicle started 
by dropping two aspirin tablets into the battery itself. The aspirin’s 
acetylsalicylic acid will combine with the battery’s sulfuric acid to produce 
one last charge. But after this don’t forget to rush to the nearest car station.<br>
<br>
A mixture of baking soda and warm water can also help eliminate the corrosive 
buildup on your car’s battery terminals. Take 3 tablespoons baking soda and 1 
tablespoon warm water scrub them clean using an old toothbrush and wipe them off 
with a wet towel and dry with another towel. Once the terminals have completely 
dried, apply a bit of petroleum jelly around each terminal to deter future 
corrosive buildup. <br>
<br>
If your car battery showing sign as if there is no more life in it give it a 
little more life try this out . Dissolve about an ounce of epsom salt in warm 
water and add it to each battery cell. This can help.<br>
<br>
Why is that in a winter morning the car battery gives up? Low temperatures 
increase electrical resistance and thicken engine oil, making the battery work 
harder. Before winter starts, disconnect the terminals and clean them&nbsp; with 
a wire brush. Reconnect, then smear with petroleum jelly. The jelly will prevent 
corrosion and help keep the battery cranking all winter long.</font></p>
