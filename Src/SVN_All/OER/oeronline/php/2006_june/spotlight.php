</font><font face="Verdana" style="font-size: 14pt; font-weight: 700">Global 
best practices on car-care</font><font face="Verdana" size="1"><b><br>
By RITWIK SINHA <br>
</b><br>
<b><img border="1" src="spotlight2.jpg" align="right" width="150" height="100"></b>Going by the popular perception, your car or vehicle is just like any other 
inanimate object of utility which is the prime function of engine, battery, 
radiator, carburetor, tyres, hundreds of other small components and most 
importantly the command of your hands on its steering. But scratch the surface 
and one can well notice the trends wherein a car or a vehicle could display the 
trends of an animated object — at least, when it comes to care and maintenance.
<br>
<br>
Like human beings, cars do respond to change in climatic conditions differently 
(for instance, battery could drop low in winters and summers can adversely 
affect the cooling system of the vehicle) and like them they also require 
adequate care to stay in top shape and gear. Needless to say, like for human 
beings, there are also globally tested prescriptions to keep cars in fine 
fettle. Following are some of the finer points of this prescription: <br>
<b><br>
Engine should be properly tuned</b><br>
One of the basic rules to keep a car in top gear is that its engine should 
always be properly tuned. For this, the car needs to be regularly checked. An 
out of tune engine can adversely affect the performance of a car especially when 
it comes to the consumption of the fuel. For instance, a simple misfiring plug 
can cause a reduction in the mileage of the car by a whooping 30 per cent. An 
out of tune car also reduces the life of the engine which simply is the heart of 
the vehicle. Therefore, this point should not be missed at any cost. <br>
<b><br>
Periodic replacement of air filters</b><br>
This is another important clog of auto maintenance process. Engine air filter 
often clogs with dirt, dust and bugs putting unwarranted pressure on the car 
engine. It can also make the vehicle less fuel-efficient. Several studies have 
demonstrated that replacing a clogged air filter improve the fuel efficiency of 
the vehicle by over 10 per cent. It is, therefore, <br>
<br>
advised to get your car’s oil and air filter replaced as specified in the 
owner’s manual (normally after 3000 miles). Moreover, engine air filter should 
be checked at each oil change and if it is showing signs of erosion, then it 
should be replaced immediately. <br>
<b><br>
Always use right oil</b><br>
Studies reveal that a car stays in top shape if its owner were to use the oil 
manufacturer’s recommended grade of motor oil. For instance, because of some 
misconception many car owners opt for premium gasoline when the advised fuel for 
their vehicle is lower-octane gasoline. This only results in loss of precious 
money without making any difference to the performance of the car. The best 
remedy therefore is to use oil specified by the vehicle manufacturer. Another 
option is to opt for motor oil with the words “energy conserving” on the API 
performance label as this grade of oil contains friction-reducing additives. <br>
<b><br>
Keeping a constant tab on air pressure</b><br>
This is a small but crucial exercise. Driving on under-inflated tires is not 
good for your car as it puts unnecessary pressure on several other important 
components including the engine. It also reduces the fuel efficiency of your 
cars by as much as 15 per cent. Furthermore, tires’ life get affected in a major 
way. It is, therefore, advised to check the air pressure of your vehicle’s tires 
at least after every fortnight and if the pressure is on the lower side, then it 
should be immediately taken care of. It is recommended to check tire pressure 
when tires are cold. An ideal time to do it is in the early morning after the 
car has been lying idle in the night. <br>
<b><br>
Fixing the fuel cap</b><br>
Not attaching enough importance to the task of tightening of fuel cap after the 
tank has been filled is a common mistake which majority of car owners make. But 
it has some very serious consequences. It results in the loss of a staggering 
147 million gallons of gas annually on global basis. The rationale is simple: 
fuel and especially gas fuel evaporate if the cap has not been tightened. It is 
recommended that all car owners should lay the required emphasis on this point 
or else they will be contributing that colossal loss pool. <br>
<b><br>
Proper parking space</b><br>
It’s a must for all car users that they should make provision for proper parking 
space for their vehicles. The ideal way would be to have a garage to park your 
vehicles. If the car is parked in a garage, it will stay warm in winter and cool 
in summer. But in case, you are space starved and can’t have a proper garage for 
your vehicle, then the alternative is to <br>
park the vehicle in the shade of a building or a tree. If the car is left 
exposed to sun light, then there are chances that fuel may gradually evaporate 
because of the heat. Also, excessive heat of the sun can affect the cooling 
system of the car.<br>
<b><br>
Cleaning of fuel system</b><br>
Fuel system deposits accumulated over a period of time often result in lowering 
the performance capability of an engine. The mileage of the vehicle also goes 
down. But it can be taken care of by adding a fuel system cleaner to gas tank 
after every oil change (or after every 5,000 kms) which enhance engine 
performance and fuel economy.<br>
<br>
The above-mentioned seven tips are applicable for all seasons and are basic 
rules which should be always followed. However, there are other sets of 
supplementary prescriptions as well to keep your car in fine shape. For 
instance, its maintenance during summers when many of us prefer to go out on 
vacations but summer heat pose several problems. Following are some tips to 
ensure that car continues to deliver smooth performance even during those 
scorching summer months: <br>
<b><br>
Check the coolant</b><br>
To check the coolant, wait until the engine is cool before unscrewing the 
radiator cap. If the rubber gasket inside the cap is cracked or brittle, it 
should be replaced immediately. Top off the coolant at the overflow reservoir, 
not at the radiator. <br>
<b><br>
Inspecting radiator hoses</b><br>
For this also, wait until engine is cool. Carefully check the ends of hoses to 
find out cracks or corrosion because they can result in leaks. In extreme summer 
situations, spongy hoses are advised. However, if any crack is noticed in 
radiator hoses, then it should be replaced immediately. <br>
<b><br>
Battery Inspection</b><br>
Contrary to popular belief, summers can inflict greater damage to a car battery 
than winters. Therefore, before embarking on a long trip in summers, one should 
minutely judge the battery condition. If its in a rusted condition, then it 
should be cleaned by applying a mixture of baking soda and water. One should 
also ensure that the battery terminals and hold downs are tight. <br>
<b><br>
Examining starting and charging system</b><br>
This again is a vital element of auto care during summers. If the under-hood 
temperature is on a higher side, then it can result in short-circuiting of car’s 
starter and alternator. But this is a test which can’t be done at home by an 
average car user and therefore it is advisable to visit a car station where it 
can be checked in a few minutes. This examination, however, is a must and can be 
ignored only at the peril of inviting troubles in a remote location. <br>
<br>
Going by the suggestions of several auto experts, the above-mentioned remedies 
on a cumulative basis are capable enough to keep your vehicle in a top shape 
irrespective of season variations and give you what you solely desire from it — 
the utmost moving comfort on the road. On second thoughts however, there is this 
parting word of advise — always pack an emergency kit that includes water, 
jumper cables, flares, a flashlight, equipment to change a tire, and a first aid 
kit whenever you decide to get behind the wheels irrespective of the fact 
whether you are going for a short or a long trip.<br>
