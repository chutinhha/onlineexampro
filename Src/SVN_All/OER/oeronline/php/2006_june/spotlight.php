<img border="1" src="spotlight4.jpg" align="left" width="150" height="74">When did you last check your car tyres? Can’t remember? Well, it is pertinent to 
remember that the only contact that your car has with the road are the four 
pieces of tyre. No matter how good you are as a driver or how good the 
performance of your vehicle is, your tyres will always affect your vehicle’s 
traction to the road. <br>
<br>
Tyres, like any other part of your car, need to be looked after. Apart from 
saving time and money, if your tyres are sub-standard in any way, they can 
severely affect your cars stopping and handling ability in emergency situations. 
All cars are different. Whether it needs high performance tyres for greater 
handling or smooth and silent tyres for the family car, one should go for tyre 
care on regular intervals.<br>
<br>
These tips are simple things that you can do to make sure that you get the most 
out of your tyres in terms of safety and longevity.</font></p>
<ul dir="ltr">
  <li dir="ltr">
  <p align="justify" dir="ltr"><font face="Verdana" size="1">Tyres should have a 
