</font><font face="Verdana" style="font-size: 14pt"><b>Steady Pace</b></font><font face="Verdana" size="1"><b><br>
By SARADA VISHNUBHATLA</b><br>
<br>
<img border="1" src="spotlight1.jpg" align="left" width="150" height="86">Auto accessory market in Oman is sailing smoothly at a steady pace. The growth 
of the accessory market segments such as tyres, batteries, lubricants and 
sunshades is in direct proportion to the growth in the vehicle volume. In the 
present scenario, the tyre-trading sector in Oman is experiencing a healthy 
growth over the last couple of years while the lubricant and battery segments 
are witnessing a moderate growth. And when it comes to sunshades, there is a 
perennial demand the world over. The overall market scenario in the Middle East 
mirrors a similar trend.<br>
<br>
The Gulf Cooperation Council (GCC) countries have traditionally been a 
profitable market for pan-nation auto car manufacturers and its accessory 
makers. European manufacturers have always dominated the lubricant industry 
world over with very few originating from Japan, and Middle East like the UAE 
and Oman. Similarly, the battery segment has brands coming in mainly from Europe 
and lately from countries like Indonesia, South Korea or Saudi Arabia with an 
indigenous brand from Oman. Unlike the above two, Oman has only tyre-trading 
industry as there is no rubber production in the country. Neither does the 
country have indigenous sunshade makers. <br>
<b><br>
Wheels of fortune</b><br>
Fortune on the wheels rolls in with a delay in any given international 
automotive market. A brand new car will need its first change of tyre only when 
it has travelled the initial distance of about 50,000–80,000 km depending on the 
model, the quality of the battery and brand of tyres installed and the purity of 
the lubricant running the engine. <br>
<br>
The growth rate of the tyre trading industry can be gauged only 2-3 years after 
an increase in car volume. In 2004, the registered car park figure stood at more 
than 500,000. By the end of 2005, the number has gone up by more than 30,000 
cars since then. According to the market estimates, the figure will go up 
further by about 10,000 cars in 2005-2006. <br>
<br>
The speculation is that it will maintain the upward curve for some more time to 
come. Demand definitely is double the supply both in the Middle East region as 
well as in Oman specifically. International tyre brands rule the roost in Oman 
with no indigenous brands. The consumer has a wide choice from top brands like 
Michelin, Pirelli, Hankook, Yokohama, Bridgestone, Good Year, Toyo, Falken, 
Kumho and Continental among others. Other GCC countries viz., Qatar, Kuwait, 
Bahrain and Dubai are also experiencing a similar growth trend. <br>
<br>
Besides being influenced by the increase in car volume, tyre consumption is also 
affected by the usage and maintenance. Middle East being the hot seat for a 
longer duration of summer season than the rest of the world, tyres are one 
accessory that bear the brunt of burning temperatures. A few good thumb rules 
can take care of the untimely change of the accessory ensuring good value for 
money to the consumer.<br>
<b><br>
Reducing friction</b><br>
In the Middle East, the lubricant industry has more than 30 international 
players. A few major companies are Shell, BP/Castrol, Mobil, Total, Elf, Caltex, 
Valvoline, Gulf Oil, ADNOC, Veedol, Crown, Fuchs, and National among others. 
Undoubtedly, Shell Oman has the largest market share in terms of total 
production. While Shell Rotella leads the market in Diesel Engine Oil segment, 
BP’s Visco 2000 rules the game in the Petrol Engine Oil segment. All of them 
cater to the Omani market, which stands at 28,000–30,000 Metric Tonnes per 
annum. With more vehicles on the road and new projects coming up, lubricant 
demand has seen a steady increase in the last couple of years.<br>
<br>
Lubricants market has three major user groups — commercial and industrial 
sectors top the list with consumption standing at 60 per cent, next in line is 
the automotive sector consuming about 35 per cent of the lubes and a mere 5 per 
cent is utilised by the marine sector.<br>
<br>
All the branded engine oils meet the high standards and best performance ratings 
that the new generation of car engines promise. But with the crude oil prices 
shooting up in 2004, the base oil prices experienced a direct impact hitting the 
lubricant sector badly. Not only did the profit margins shrink for the major 
brands, the segment was flooded with a steady inflow of spurious and low-quality 
brands from countries like Indonesia and UAE. Priced at less than half the cost 
of the premium brands, the low-specification brands and look-alikes managed to 
grab an undue albeit small share in the market. Major brands had no choice but 
to cut down on the prices. Brands like Camel, Stanley, and Euro Oil resort to 
using false quality stamps for an entry. <br>
<br>
Sadly, these brands find takers not only in the individual customers but also in 
big organisations many a time, which are cost-conscious. Low-grade oil has 
insufficient additives effecting engine’s functionality and life resulting in a 
potentially high performance car working under par. Branded oils go through 
stringent quality tests to ensure longer engine life and reduced costs on 
maintenance. Sub-standard oils fail on both the counts.<br>
<br>
In mid-2004, the Directorate General of Specifications and Standards under the 
Ministry of Commerce and Industry announced a decree as an effort to contain the 
spread of bad oils. The grade set for the import of a lubricant was decided at 
API SJ Quality for petrol engine oil and CF Quality for diesel engine oil. While 
the law is in existence, slack approach in its execution is allowing the 
low-grade lubes to find their way into the country. But the majors in the 
segment are hopeful that soon the loopholes would be identified and plugged 
boosting a healthy market again.<br>
<br>
Apart from the battle with spurious brands, the biggies also have to contend 
with the lubricant re-use market in Oman. The oil that is dumped at the auto 
workshops and service stations is recycled and sold at a cheaper price to the 
cost-sensitive consumers owning handed-down cars or even worse to the people who 
do not care what grade oil goes into their cars. Not only does the recycled lube 
affect the engine’s health, it also causes environmental pollution. Cross 
checking at the user end acts as the best deterrent in the usage of recycled or 
sub-standard lube.<br>
<b><br>
Cranking it up</b><br>
Another important auto accessory is the battery. If cared for properly, 
batteries last a long time offering higher drain intervals to the car. Sadly, 
this accessory remains the most ignored part of the car, unlike the market 
segment itself. <br>
<br>
In the last couple of years, the vehicular volume has seen a steady growth at 
4-5 per cent and the growth of battery market segment is hand in hand with it. 
The European giants have dominated the battery market segment in Oman 
traditionally. The worldwide biggies in the field viz., Exide, AC Delco, Yuasa, 
NBC, GS and Rocket among others, share an almost equal footing in the Omani 
market. The market boasts of only one brand Reem Batteries (Antara) that is 100 
per cent local product. <br>
<br>
A few brands coming in from countries like Indonesia and South Korea offer good 
competition but there are many others that manipulate the market price by 
introducing low-quality gear. Low price is enabled with reduced lead layers in 
the batteries. Moreover, incentives like wider margins to the dealers and auto 
electrical shop owners help them to push their product in to the hands of those 
unsuspecting customer who are more cost-sensitive than quality-conscious. On the 
other hand, many consumers realise the worth of a good battery and pay a bit 
extra to get a fine product.<br>
<br>
But the major brands have a two-fold advantage over the low-quality names. First 
is the reach and the second is the range of products. The premium brands 
invariably build their brands through strong distributor network. The product 
range that standard batteries can afford to manufacture is difficult for the 
sub-standard brands to match. Then there is also brand-loyalty aspect that plays 
a significance role. For example, Reem Batteries (Antara) being an Omani company 
commands a tremendous goodwill among the locals and the brand boasts of the 
widest range of batteries for every kind of a need reaching the customer through 
its strong distributor network. <br>
<br>
Battery care in a hot climatic region like Oman is as important as caring for 
the engine itself. The most common failure on part of the consumer is checking 
the water level in the battery. Even the auto experts are at fault in this 
aspect. In peak summers, batteries need to be checked for the water level at 
least once a month, so that it does not drain half way through its lifetime or 
midway to an important meeting in the searing heat. While buying a new battery, 
especially the dry charge cell, the consumer should wait for the mandatory 
period of thirty minutes before installation. This is because the lead acid that 
has been added to it requires time to charge the battery. This way the drain is 
increased minimizing falling rate. Another most common failure witnessed is 
ignoring company specification to use a suitable size of the battery. It should 
match the ampere requirement of the vehicle giving you value for money. If a 
consumer finds his car refusing to crank first thing in the morning, it is an 
indication that either the battery or other electrical parts need to be checked 
for their functionality immediately. <br>
<b><br>
Cool Effect</b><br>
Globally and in Oman, the market leader in sunshades is Germany-based V-Kool. 
With a presence in more than 25 countries, the premium brand claims to block 
infra-red by 94 per cent, UV rays by almost 99 per cent while keeping visibility 
clear at 73 per cent. Locally, the brand competes with names from India, USA and 
China; like Teejan, Ziebart, Solar Guard, Johnsons, Amcool and Emcool. The 
low-grade brands work on the concept of absorbing the heat and harmful rays 
while the premium brand reflects, cutting out on the root cause itself.<br>
<br>
Total glass surface area of a car and the type of film chosen dictate the cost. 
For a standard-size full car, a good sunshade will cost minimum RO 100 while a 
low-quality film can be as low as RO 3. The market leader offers five types of 
warranty on car sunshades. With zero maintenance and Sun Protection Factor (SPF) 
at 230+, V-Kool is tested and approved by Directorate General of Specifications 
and Measurements under the Ministry of Commerce and Industry. <br>
<br>
A little care and attention to your car and its valuable innards go a long way 
in ensuring a cool and comfy ride. Who then cares if the sun chooses to beat it 
down hard! Come summers, we are ready for you.<br>
