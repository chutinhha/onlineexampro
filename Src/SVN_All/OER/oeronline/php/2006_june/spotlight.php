</font><font face="Verdana" style="font-size: 14pt"><b>Sun(e)scape</b></font><font face="Verdana" size="1"><b><br>
By Monalisa Nayak </b><br>
<br>
Summer heat is on and we take all possible care against the fiery sun with 
expensive and efficient sunscreens and ultra cool sunglasses. But what about our 
vehicles? Especially, in a hot region where most people travel by car? Taking 
