</font><font face="Verdana" style="font-size: 14pt"><b>Lubricate Your Engine</b></font><font face="Verdana" size="1"><br>
<b>By Monalisa Nayak </b><br>
<br>
<img border="1" src="spotlight3.jpg" align="left" width="150" height="129">Majority of modern day vehicle owners today are aware of a vehicle requiring 
proper maintenance for functioning well but yet very few of them are conscious 
of certain vital subjects of maintenance like lubricants. Not many know what 
lubricants are, what its main functions are and what kind of lubricants they 
should use. <br>
<br>
Different types of engines require different types of lubricating oil depending 
on the age of the car, the type of car, the driving conditions and whether the 
car has a diesel or petrol engine. Car lubricants play four major roles — they 
control friction and wear in the engine, they protect the engine from rusting, 
they cool the pistons, and they protect the engine oil stored in the sump from 
combustion gases. Moreover it also helps to absorb some of the harmful 
by-products of combustion, and to transfer heat from one part of the engine to 
another. <br>
<br>
Using good lubricants will provide the motorist with the following benefits: <br>
• Easy starting <br>
• Better fuel consumption resulting from lower friction <br>
• A cleaner engine as a result of high-disperancy detergents <br>
• Reduced oil oxidation and resultant oil thickening <br>
• Lower oil consumption<br>
• Reduce wear protecting against scuffing<br>
• Excellent rust and corrosion protection for your engine <br>
<br>
A modern contemporary three box car has thousands of parts and well over 1,000 
parts of them are synchronised to move together within extreme tolerances. Under 
such demanding conditions the role of lubricants can never be ignored or over 
emphasised. <br>
<br>
For many of a vehicle’s components, the lubricant plays a vital role as a design 
element. Because wherever there is relative motion between machine components in 
contact, specialty lubricants reduce wear and tear, alleviate friction, and 
enhance the functioning of the component concerned. Some 75-95 per cent of a 
typical engine lubricant is made up of a base oil - a mineral oil that has come 
directly from a refinery. These base oils can naturally contain straight or 
branched chains of hydrocarbons, hydrocarbon molecules with aromatic rings 
attached, or these chains can be produced by further chemical reactions of the 
base oils. Of all lubricants, engine oil is the most important. It keeps all the 
deposit forming material in suspension and gets rid of them by oil filter, or 
draining out at proper intervals. <br>
<br>
The remainder of the lubricant comprises a variety of additives, which are used 
to improve performance. Typically these include anti-wear additives, corrosion 
inhibitors, antioxidants, detergents, dispersants, antifoam additives, and large 
polymer molecules known as viscosity modifiers, which are added to improve the 
viscosity variation of the lubricant with temperature. <br>
<br>
Indeed, the viscosity is the most significant physical property of a lubricant. 
The way in which it varies with temperature, shear rate and pressure determines 
to a great extent how the lubricant performs in an engine. But the chemistry of 
the lubricant is also important: it must be resistant to oxidation and it must 
be able to “lay down” a protective film to combat wear and tear where metallic 
contact is inevitable. Lubricants fall into two broad categories — monograde and 
multigrade — depending on whether their viscosity changes significantly with 
temperature or not.<br>
