<p align="justify" dir="ltr" style="margin-left: 10px; margin-right: 10px"><font face="Verdana" size="1">
<br>
<br>
</font><font face="Verdana" size="5">
<b>What’s in a corporate&nbsp; name?<br>
</b></font><font face="Verdana" size="1"><br>
<b>Naseem Jawed, author of the famous book ‘Naming for Power,’ explains, in an 
exclusive article for OER, why corporate leadership via image and identity is so 
critical for Oman</b><br>
<br>
<b>
<img border="1" src="by_invitation1.jpg" align="left" width="100" height="106"></b></font><font face="Verdana" style="font-size: 11pt"><b>T</b></font><font face="Verdana" size="1">oday, it’s all about building 
image. Business name identity, high visibility, and global e-commerce are the 
key terms. Global recognition with instant accessibility on the net, quick 
search-ability on the web, distinct ‘memorability’ of names by an overly 
strained populace, easy ‘typability’ by tired fingers, and pleasant vocalization 
of such names and brand experiences by the customers all over the country and 
the region. The key lies in image branding. And most importantly, in the 
corporate name identity! <br>
<br>
This new race for creating powerhouse corporate brand personalities in Oman and 
other GCC players has just started. Dubai leads the charge by opening many new 
gates and frontiers. Then, universally speaking, we have already entered a 
name-economy, where economic power is determined by the number of powerful 
global brands that a country positions against the other, and a region against 
another. This is what Japan and the US did in the last decades. Now come the new 
Asian brands.<br>
<br>
This new name-economy is easily the new driver of e-commerce. It’s the only 
boost to global cyber-branding as a most powerful tool to dominate any market or 
a region. At this very second, business names are skating at bullet speed on 
this flat new earth, without borders, passports or time zones. No delays, no 
barriers, no major costs. Just plain access! <br>
<br>
The name identity of a business will be the only measure on how a name works in 
a micro-multi-national-formation in a maze of countries and cultures. Economic 
power will be defined by the cyber-brand power and its accessibility in number 
of countries. The traditional, big visuals are replaced by small URLs. Under the 
new rules, a name works like a KEY, being the only thing that can unlock the 
doors to your corporate-kingdom. <br>
<b><br>
CHANGE NOW, CHANGE LATER<br>
Why do corporate images die a slow death?</b><br>
Globally speaking, every hour, around the globe, seven days a week, a major 
corporation is forced to change its name. Sometimes it’s good news (when there 
is new adventure in the air). But at times — and often — it’s pretty bad news. 
That’s when a corporate name becomes a liability, a burden — injured in a 
trademark battle — when simply it is no longer capable of ringing the cash 
registers. Here are seven reasons why.<br>
<b><br>
ONE: Corporate name is identical or similar to thousands of others:</b><br>
When a corporate name is heavily diluted and shared by hundreds and thousands of 
others in all kinds of businesses, then a name is simply lost in the crowd. 
Also, when the name is a borrowed word from dictionary — making it part of 
everyday lingo — it never achieves any distinction. And despite extraordinary 
expenses in advertising and promotion, it simply dies out of exhaustion. Open 
any old business magazine and it will unfold like a cemetery of dead corporate 
names. <br>
<b><br>
TWO: Name is too old to convey today’s dynamics:</b><br>
Established as a great icon of a period, sometimes a name crawls out of history. 
It may have reflected the great human toil by the founding fathers, but somehow 
it’s not at all suitable for the current crop of agile ‘cybernauts’ and the 
digital-savvy faces, surrounded as they are with emerging technologies that will 
propel them to an explosive future. Corporate communication constantly struggles 
to shed the old image by promoting the future vision of the company and tries 
hard to appear young and futuristic. But, like a chronic, grumpy, old patient in 
a nursing home, these types of names struggle and slowly linger for decades. But 
they always fade away in the end. Any old publication will provide the proof in 
black and white.<br>
<b><br>
THREE: The spelling of corporate name requires a higher IQ: </b><br>
A large majority of corporate names are spelled creatively to fit a logo or to 
avoid a serious trademark problem. Here, common sense and the science of 
corporate nomenclature are abused at the risk of being too sexy and different. 
This twisted spelling only ensures obscurity. The mind continually rejects the 
corruption of a familiar word and refuses to remember specific alpha structures. 
After all, if a name can be spelled in four different ways, you will only end up 
with 25 per cent of the hits and profits. This type of creativity doesn’t help. 
These sparks of geniuses end up kindling fires and eventually causing serious 
damage. <br>
<b><br>
FOUR: More money spent explaining the origin of corporate name:</b><br>
If a name cannot simply relate to the business and requires constant explanation 
of its obscure, yet cute, origin and how it fits overall to the business, then 
it becomes a daily routine for advertising to educate the universe of this 
‘dysfunctionality’. The poor consumer, the lost end-user, and the over-burdened 
population of the world at large, doesn’t really care what the name means to 
you, rather simply, what it means to them. Corporations and ad agencies thrive 
on getting awards for their creative efforts and advertising gimmicks for 
pushing their side of the story, while the consumer simply shuts off.<br>
<b><br>
FIVE: Do not own a trademark with an identical dot.com: </b><br>
If you do not legally own a corporate name, then what’s the point of the 
exercise? Why bother at all? Every time your name is advertised, it simply helps 
the industry at large, including your competitors. Also, you don’t build any 
brand values and equities and your entire advertising and marketing dollar is 
wasted. A large majority of corporate names in business today are not globally 
‘trademarkable’. Most do not have identical universal dot.com domain.<br>
<b><br>
SIX: Corporate name is embarrassing in certain countries: </b><br>
Globalization is a fact of life. A name must work like a marketing weapon, not 
only in your own country but also around the globe. There is no need to hide 
under the desk because the name is embarrassing or profane in a foreign 
language. Today, a large majority of names do not work efficiently on the 
international scene and cause an ongoing stress in gaining international 
recognition.<br>
<b><br>
SEVEN: Name is too long, too difficult, too complicated or too boring:</b><br>
When a name is too long, it gets initialised. This unwanted process changes the 
entire meaning and lists the name in strange categories. When a name is too 
difficult, confusing, or boring, it becomes a different animal to different 
people. Strange name combinations end up telling more than one story, causing 
confusion in the marketplace. Weird terminologies, alphanumeric structures, 
using upper or lower cases, dashes or slashes and other dingbat characters in a 
name, will only ensure self-destruction. <br>
<b><br>
Global Name Identity<br>
Why it is so essential to have a unique and hassle-free global identity?</b><br>
Today, naming is no longer a simple creative exercise. It’s rather a serious 
strategic discipline now where the Rules of Naming and the Laws of Corporate 
Nomenclatures must be applied. There are language considerations too. There are 
trademark laws and rules of global domain management laws, plus connotations and 
marketing understanding to fit the right profile of the corporation with the 
right potential customers. There is a big difference between a massive 
advertising-based branding exercise and a highly specialized name branding 
expertise. <br>
Mega-failures in the West due to poor naming has taught some very valuable 
lessons. Corporations struggle to re-configure their branding trees of naming 
architectures. Trademark lawyers often get the bad wrap for unnecessary fights. 
But these battles are only created by the naïve executives who ignore the rules 
of naming in the first place and go for a seat-of-the-pants-name-ideas and fly 
it in a panic, often hours before the press conference. A recent study showed 
that 97 per cent names are selected on the exact day of the announcement, hours 
before the press conference! <br>
<b><br>
Nationalization With Great Brands Out of GCC <br>
To attract the attention of the locals, a global personality must be created.</b><br>
Review &amp; Audit: What good are businesses and corporate name identities if they 
don’t grab regional and international buzz? What good are expensive ad campaigns 
if they still keep the original message lost and the images in the dark or 
invisible? Simply, execute a professional audit, not by the same people who are 
currently running your image and branding, but rather by applying the 5 Star 
Standard and cyber branding rules. Have a very candid, frank and open 
discussion.<br>
<br>
Focus &amp; Aim: What good are campaigns if nobody remembers or understands them? 
What good are the selling propositions if they are off target and/or out of 
scope? To nurture a sophisticated series of selling ideas and their related 
delivery systems requires brand new sets of skills. Aim high or aim low but aim 
very clearly. This will tremendously boost internal and external communications, 
moral and productivity. Nationalization demands clear and powerful messages to 
attract the local interest. Go through an image and name identity leadership 
process and bring in the best expertise.<br>
<br>
Start Fire: You may also just simply fire-up the current set-up that’s dragging 
your corporate image and branding strategy. Face global marketing challenges 
head-on. Any insecurity or doubtful actions based on the old marketing school of 
thought will seriously fail. Out there, it’s all very new. On one side is a very 
flat earth and on the other is a very new race, both offering extraordinary 
opportunities. <br>
<br>
Best, get the right team with the right knowledge and explore how to maximize 
your image and identity in the shortest time with the maximum bang. Oman and 
other GCC corporations have very unique issues and require very clear answers, 
backed by solid experience and global professionalism.<br>
