                  <p align="justify" style="margin: 10px"><font face="Verdana" size="1">
                  Orin’s approach couldn’t be more different than mine. Orin is 
                  quiet and reserved and almost always in his shell, like a 
                  tortoise, and he works steadily and faithfully at problems 
                  until they get solved. He always carries a pen and notebook in 
                  his pocket, and when he wears his big-framed glasses, he looks 
                  as wise as he is. When a dilemma arises, I tend to make a snap 
                  judgment and want to take action immediately, while Orin 
                  listens calmly, gathers all the information he needs and 
                  ponders carefully until he comes up with a logical, reasoned 
                  response. <br>
                  <br>
                  When Orin came to us in 1990, Starbucks was not a 
                  professionally managed business. We were totally 
                  entrepreneurial with an approach Eric Flamholtz, one of our 
                  consultants, characterizes as “Ready, Fire, Aim.” To his 
                  credit Orin didn’t make a big show of turning Starbucks into a 
                  professionally managed company. If he had, it probably would 
                  have spooked me and many others inside the company. Instead he 
                  led by example. Thanks to his equanimity and his leadership 
                  skills, the organization began gravitating naturally towards a 
                  more balanced approach- just as we got big enough to need it.<br>
                  <br>
                  Very subtly, he created an environment in which there was for 
                  the first time a strong appreciation of the disciplines 
                  necessary to run a large and profitable business. He built an 
                  organization by recruiting seasoned professionals in the key 
                  areas that had to be strengthened in the company: management 
                  information systems, finance, accounting, planning, legal 
                  affairs and supply-chain operations. <br>
                  <br>
                  However warily I began to recognize that in building 
                  discipline into a company, its possible to not only honor the 
                  creative process but also make it stronger and more dynamic. 
                  By strengthening the foundation and structure of the company, 
                  we can stop wasting time reacting to small problems and 
                  instead devote our attention and resources to new products and 
                  new ideas. With a clearer strategic direction, we can focus 
                  our creativity on issues that matter for Starbucks in the long 
                  term.</font></td>
