            </font><font face="Verdana" size="4">
            Pour Your Heart Into It</font></b><font face="Verdana" size="1"><br>
            <br>
            <img border="0" src="../2006_june/browsing_corner.jpg" align="left" width="142" height="239">Unlike 
            most other business books that analyse or theorise about business 
            strategies and empires, it’s very important to understand, at the 
            very beginning, that this book is not about all those aspects! ‘Pour 
            Your Heart Into It’ by Howard Schultz and Dori Jones Yang is a book 
            that reads quickly. When you finish reading it, you sit back and 
            wonder what indeed was it all about then. That’s when you will need 
            to go back to the Prologue and you’ll get the entire message in a 
            nutshell: <br>
            <b>“Care more than others think wise.<br>
            Risk more than others think safe.<br>
            Dream more than others think practical.<br>
            Expect more than others think possible.”</b><br>
            This is exactly what the Chairman and CEO of Starbucks did to build 
            his company, literally, one cup at a time. It is the story of the 
            progression of a coffee house started by two teachers and a writer — 
            three people passionate about coffee and a chance meeting with 
            Schultz who dreamt about turning this passion into an international 
            business enterprise. He persuaded them to rope him in despite their 
            initial reluctance to let their small enterprise grow. Later, they 
            dropped out to remain more in line with what they had originally set 
            out to do while Schultz dreamt even more to make Starbucks the way 
            we see it today.<br>
            <br>
            Initially, Schultz dreamt and persuaded people but then to make it a 
            reality, he risked a steady job in a large firm to move to something 
            he felt was his calling — his destiny. As he went along the road to 
            pursue the vision he had brought to life, he took care of the people 
            who helped him make it all happen — from the topmost executives whom 
            he hired to the ‘baristas’ who actually linked him with his 
            customers, who in turn appreciated and savored every cup that was 
            brewed at Starbucks. As a result, he expected nothing but the best 
            from himself, his staff and business. <br>
            <br>
            The bottom line was quality at the cost of everything else - quality 
            of the product, service and ways of doing business. Nothing was 
            spared when it came to looking after his people. He is even candid 
            about hiring people better than himself to do the jobs he couldn’t, 
            and then, leaving it entirely to them.<br>
            <br>
            He narrates in detail how he maintained the standard during testing 
            times when the market was hit by frost in Brazil and how he had to 
            hire the best in the business to help him make coffee drinking seem 
            pleasurable and romantic. What seems effortless is indeed hard work 
            round the clock — read the book and find out what a host of 
            professionals had to contribute to build the Starbucks brand that 
            has created a revolution in the coffee business. <br>
            <br>
            Problems such as growth versus quality - inevitable to every 
            business — were tackled carefully. Starbucks steered from its core 
            business of serving coffee only brewed by their own home-trained 
            baristas to hotels, airlines and fast food chains — all initially 
            against its main principle of brewing coffee one cup at a time and 
            that too on home turf alone. Now, music, ice cream and more is made 
            available at Starbucks. Dogmatic thinking proved disastrous and 
            flexibility was valued to eventually please the customer.<br>
            <br>
            For a book that rambles along, and doesn’t claim to be a 
            conventional business book, nevertheless illustrates the story of a 
            phenomenal company and its constant goal to balance passion with 
            profit. Budding entrepreneurs, sales and marketing teams as well as 
            companies on the threshold of expansion plans can greatly benefit. 
            So, go ahead and pour your heart into it!<br>
            —Uma Devi Jadhav</font></p>
