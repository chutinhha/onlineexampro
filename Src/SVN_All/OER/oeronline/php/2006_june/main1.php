<p align="justify" dir="ltr" style="margin-left: 5; margin-right: 5"><b><br>
</b><font face="Verdana" size="5"><b>Worst for US Dollar is yet to come<br>
</b></font><font face="Verdana" size="1"><b><br>
Standard Chartered Bank Head of FX Strategy, Global Research, Callum Henderson 
speaks to Vani Saraswathi in Doha about the increasing importance of hedging, 
and how corporates need to balance their risks. Excerpts:</b><br>
<b><br>
</b></font><font face="Verdana" style="font-size: 11pt"><b>S</b></font><font face="Verdana" size="1"><b>ince 
GCC currencies are pegged to the US Dollar, how beneficial will hedging be?</b><br>
</font><font face="Verdana" style="font-size: 11pt"><b>
<img border="1" src="face2face.jpg" align="left" width="100" height="114"></b></font><font face="Verdana" size="1">The  </font><font face="Verdana" size="1">very concept of hedging foreign exchange (FX) risk is a relatively recent 
phenomenon in the Middle East, particularly in the Gulf Cooperation Council 
(GCC). Traditionally, companies have generally taken a laissez-faire approach to 
FX risks. However, with many currencies pegged in some way, fundamentally, this 
made a lot of sense, especially for most GCC states, as their main exports and 
much of their economy is linked to oil and gas — which is priced in US Dollar. 
Consequently, it made sense for their currencies to be pegged to the US Dollar. 
However, the idea that FX transaction risk can be managed and hedged is gaining 
some traction in the region. <br>
<br>
There have been several reasons for this, including globalisation, technological 
innovation and gradual financial liberalisation. But one important reason which 
many do not necessarily focus on is the fall in the US Dollar from 2002 to 2004. 
While exports are largely priced in US Dollar, this is not the case for imports. 
Therefore, the ensuing rise in currencies such as the Euro and Japanese Yen 
against the US Dollar led to a sharp rise in non-US Dollar import prices.<br>
<br>
At the macroeconomic level, this hit domestic purchasing power. At the 
microeconomic level, this hit the bottom line of Middle Eastern corporate 
importers who were unable to pass on the full impact of non-US Dollar currencies 
to domestic consumers. As a result, against a backdrop of growing financial 
liberalisation, the incentive to hedge imports grew significantly.<br>
<br>
Meanwhile, Asian currencies have rallied against the US Dollar and thus against 
pegged GCC currencies. In the wake of the sharp rise in Euro, some importers 
moved away in favour of Asian and US goods. The subsequent rally in Asian 
currencies is a further potential threat to the bottom line. <br>
<b><br>
Why should corporates start hedging now?</b><br>
Just because local currencies are pegged does not mean there are not changes 
outside of that. Say, you are an importer of goods that you sell locally. If you 
are buying goods from Europe, you need to buy Euros. You are based here, so your 
invoicing is going to be in US Dollar or the local currency. But what happens 
when the Euro moves against the US Dollar — the Euro goes up, it becomes more 
expensive to buy. This is the basic premise behind why hedge FX at all, let 
alone go into individual structures. So, now there has been a general 
realisation for need to hedge FX reserves. <br>
<br>
That realisation has escalated dramatically from 2001-2004 because the US Dollar 
fell sharply and importers who did not hedge suffered as they lost 
competitiveness. FX risk is something that has to be managed.<br>
<br>
The rising incentive to hedge FX and interest rate risk, coupled with increasing 
market volatility has resulted in a significant increase in client interest in 
hedging solutions. The first wave of this led to a pick up in hedging using 
plain vanilla forwards and options — where say, the Euro is bought three or six 
months forward. The next stage was when corporate treasury in the region became 
more sophisticated and where they said, ‘Yes we realise our risk. I see the cost 
of vanilla structures, and I want to reduce or eliminate that cost.’ So, we are 
looking at either reduced or zero-cost structures. You cannot hedge 100 per cent 
and get zero-cost structures, so you hedge a part of your potential risk. Hence, 
now the focus was more towards structured products. A key component of this 
process is education in terms of hedging processes, instruments and solutions.<br>
<b><br>
How aware are regional corporations of FX risks?</b><br>
There is a general increase in corporate interest in sophisticated structures. 
So, banks are providing the necessary services — the demand has created the 
supply. A demand for solutions, corporate requirements and appetite for hedging 
products vary hugely. There is an apparent reluctance within the region to pay a 
premium on exchange rate option structures, something that we have seen at 
different stages of development in other emerging financial markets. As a 
result, zero-cost structures have become popular. However, as we always say, 
zero cost does not mean zero risk. The hedging cost should always be measured 
and offset against the extent of risk hedged. Running scenario analyses and 
creating efficient frontiers, comparing hedging strategy/cost vs. risk hedged is 
the best way to come to an informed and effective decision.<br>
<b><br>
Does the size of the corporation matter in deciding to hedge or not?</b><br>
No, size doesn’t matter. It is a principle of managing risks — in this case FX 
risks. At the end of the day, the key aspect of any successful hedging solution 
is to match an appropriate risk management strategy to the client’s specific 
exposures and risk management style as outlined in the corporation’s overall 
policy for managing currency and interest rate risk. Within this, it is critical 
that a corporation establishes a set of core principles for managing risk in 
order to provide a sound framework for actual risk management.<br>
<b><br>
Is this the end of the road for GCC currency to the US Dollar?</b><br>
There are two perceptions about the GCC currency pegs to the US Dollar. The 
first is that they have worked very well in the past, given the fact that the 
vast majority of the region’s exports are denominated in US Dollar. The second 
is that the volatility in economic performance is a natural part of a reliance 
on a single commodity whose price is volatile. Our view is quite different.<br>
<br>
Even a Stopped Clock is right some time. If you compare GCC Nominal GDP growth 
to the Fed Funds rate, it shows that it is very difficult to argue that currency 
pegs have helped monetary policy or indeed that monetary policy has worked well. 
At a time when local economies needed low interest rates, they have often had 
high interest rates — such as the early 1980s — and when they needed high 
interest rates, rates have been low or negative in real terms — such as the 
current state of affairs. Naturally, there have been times when monetary policy 
is appropriate. However, to us this looks very much like the phenomenon of the 
stopped clock being right twice a day. Thanks to the US Dollar currency pegs, 
domestic interest rates are effectively determined by the Federal Reserve.<br>
<br>
However, the outlook for the GCC economies is not part of the Fed’s mandate when 
setting interest rates. It only cares about the outlook for the US economy. 
Therefore, it is hardly surprising that interest rates set by the Fed may be 
inappropriate for the economic conditions of the Gulf countries. <br>
<br>
The usual retort to such analysis is that the GCC’s heavy reliance on oil means 
that having a peg to the receipts of this product effectively eliminates the 
hedging concerns of oil producers, resulting in greater economic efficiency. 
This is great in theory. However, the lack of currency flexibility to the US 
Dollar in reality means 100 per cent of the oil price fluctuation is imparted on 
the local economy. This can best be viewed by two examples. In the slowdown 
following the late 1970s oil shock, the GCC economies nearly halved in size as 
the ensuing global recession reduced demand and thus oil prices.<br>
<br>
Currently, we are seeing the reverse. Since 1998, when oil prices temporarily 
hit single digits and GDP was barely above that seen in 1981, the regional GCC 
economy has expanded by some 137 per cent. Most government revenue is 
US$-denominated whereas spending on goods and services is frequently based on 
non-US Dollar imports. The resulting currency mismatch could prove an incentive 
for regional authorities to diversify FX reserves out of the US Dollar.<br>
<b><br>
Some commentators say that the worst is over for the US Dollar...</b><br>
No, the worst is yet to come. Our base case is that the US Dollar will fall 
across the board, but particularly against Asia. <b><br>
<br>
What role should the Central Banks play in encouraging corporations to manage 
risks?</b><br>
There is growing interest in learning about FX and interest rate hedging 
structures and techniques. Regulators can help in this process by requiring 
regional corporate treasuries to have knowledge of hedging products in order to 
be able to manage risk more effectively.<br>
<br>
Removing the pegs would allow the authorities to determine monetary policy 
settings according to local conditions rather than delegating that 
responsibility to an organisation that has no mandate to even consider what is 
happening in the Gulf countries when it determines the interest rates it 
implicitly imparts to them. We believe this becomes even more important as 
diversification efforts start to show through.</font></p>

