<b>Disc golf: Flight of fancy <br>
</b>
</font><font face="Verdana" size="1">
<b><br>
The game of disc golf is played just like real golf with basically the same set 
of rules and regulations. Still, it is different from regular golf. Find out 
more about it</b><br>
<br>
</font><font face="Verdana" style="font-size: 11pt">
<b><img border="1" src="golf_update.jpg" align="left" width="150" height="96">E</b></font><font face="Verdana" size="1">ver tried your hand at disc golf? 
One more poser: any idea what disc golf is? Put simply, it is played very much 
like the traditional golf. However, instead of a ball and clubs, players use a 
flying disc or Frisbee. That’s disc golf for the uninitiated. <br>
<br>
The sport was formalised in the 1970’s. It shares with “ball golf” the object of 
completing each hole in the fewest number of strokes (or, in the case of disc 
golf, fewest number of throws). A golf disc is thrown from a tee area to a 
target, which is the “hole”. <br>
<br>
Now, the hole can be one of a number of disc golf targets; the most common is 
called a Pole Hole, an elevated metal basket. As a player progresses down the 
fairway, he or she must play each shot from the spot where the previous throw 
has landed. The trees, shrubs, and terrain changes, located in and around the 
fairways, provide challenging obstacles for the golfer. Finally, the “putt” land 
in the basket and the hole is completed. <br>
<b><br>
Disc Golf vs Traditional Golf</b><br>
Disc golf shares the same joys and frustrations of traditional golf, whether 
it’s sinking a long putt or hitting a tree halfway down the fairway. There are a 
few differences, though. Disc golf rarely requires a greens fee. You probably 
won’t need to rent a cart, and you never get stuck with a bad “tee time.” For, 
it is designed to be enjoyed by people of all ages, gender, and economic status.<br>
<br>
The Professional Disc Golf Association (www.pdga.com), with over 16,000 members, 
is the governing body for the sport, and sanctions competitive events for men 
and women of every skill level, right from the novice to the professional. 
Permanent disc golf courses are found in many countries worldwide.<br>
<br>
One of the great features that disc golf shares with traditional golf is that 
they are both played in beautiful settings. A nine-hole disc golf course can be 
established on as little as 5 acres of land, and a championship-caliber 18-hole 
course on 30 to 40 acres. Disc golf courses can coexist with existing park 
facilities and activity areas. The ideal location combines wooded and open 
terrains, and a variety of topographical change. <br>
<br>
The need for more courses is constant, as the sport continues to grow in 
popularity. The PDGA has created standards for the design and installation of 
new golf courses to ensure their success in the community. <br>
<b><br>
Good Exercise</b><br>
The ongoing fitness boom finds more and more people taking up recreational 
activities in an effort to improve health and quality of life. Disc golf 
provides conditioning and aerobic exercise to the upper and lower body. It 
promotes a combination of physical and mental abilities that allow very little 
risk of physical injury. <br>
<br>
Concentration skills increase by mastering shots and negotiating obstacles. 
Players of limited fitness levels can start slowly. They can gradually increase 
their level of play as fitness improves. <br>
<br>
Scheduling is flexible. One round takes an hour or two. If played alone, it 
eliminates the difficulty of scheduling tee times. And as in traditional golf, 
disc golfers find “hooked,” pumping up the likelihood of frequent participation.
<br>
<br>
Disc golf offers year-round fitness. Perhaps, the greatest attribute of the 
sport is the expense, or rather, the lack of it. A professional quality disc 
costs less than US$10, and it only takes one for basic play. And, of course, 
there’s the sheer fun of the game. No matter what your age or skill level! 
