            <br>
            HEADY YEAR</font></b><font face="Verdana" size="1"><b><br>
            After four years on top, Shell Oman concedes its number one position 
            in the OER Top 20 ranking to Omantel</b><br>
            <br>
            <img border="0" src="shell.jpg" width="180" height="189" align="left" hspace="5"></font><font face="Verdana" style="font-size: 11pt"><b>D</b></font><font face="Verdana" size="1">espite 
            achieving a “commendable performance” and engineering a “substantial 
            growth in shareholder value” in 2005, Shell Oman Marketing Company 
            SAOG has had to concede the coveted top slot to competition. Not 
            from oil marketing rivals, but to the new darling of investors viz., 
            Omantel. Thus, Shell Oman has been pushed to the Number 2 slot for 
            the first time, after being the numero uno consecutively for the 
            past four years. <br>
            <br>
            However, it has been a noteworthy year for the company in many 
            respects. “2005 was an outstanding year in all aspects of business,” 
            says the Chairman, Dr Andrew Wood. He feels proud for several 
            firsts: a record 40 per cent growth; new historic best Company Lost 
            Time Incident-Free record of 587 days, highest year on year growth 
            in the retail business, highest Omanisation, and GCC Ministerial 
            award for investment in HR and Omanisation. A heady year, indeed!<br>
            <br>
            Bottom line: The adoption of a cost management-led strategy helped 
            Shell Oman deliver a net income after tax of RO 7.46 million — 40 
            per cent higher over the previous year. During 2005, Shell Oman saw 
            its turnover move up by a hefty 31.29 per cent: from Rs.139.8 
            million in 2004 to RO 183.5 million in 2005. <br>
            <br>
            During 2005, the shares of Shell Oman listed on the Muscat 
            Securities Market swung between RO 5.100 in January to a high of RO 
            8.600 in December, before closing the year at RO 9.090. Shareholders 
            walked away with a full payout of the company’s profits after tax: 
            RO 0.750 baiza per share. Shell Oman concedes that the 2005 pay was 
            “lower than the total dividend paid for 2004, which included the one 
            off interim dividend issued from previous years’ retained earnings”. 
            This year’s final payout of 75 per cent is indeed higher than the 
            previous year.<br>
            <br>
            Business: Of its four core business activities, retail business as 
            usual accounted for 65 per cent of its total revenue, followed by 
            commercial fuels (18 per cent), lubricants business (11 per cent) 
            and aviation business (6 per cent). Dr Wood does not mince words 
            looking into the rearview mirror. According to him, aviation 
            business gained through “better adaptation to the needs of its 
            customers and through effective tendering”. <br>
            <br>
            Policy initiatives seem to have been making Omani airports more 
            attractive for foreign airlines. In the commercial segment, Shell 
            Oman’s better understanding of customer needs helped it achieve 
            better results. While conceding that retail rivalry was at its peak 
            — the total industry network grew by 20 new sites in 2005 — yet, 
            Shell Oman chose not to open any new retail site. Why? The prime 
            focus, argues Dr Wood, “was upon improving various elements of basic 
            execution”. In the blending segment, effective distribution 
            activities and improved market penetration (South and Central Asia, 
            the Arabian Gulf, Iraq and East Africa) helped growth in sales 
            volumes to record levels. <br>
            <br>
            Best Practices: Typical of a market leader, Shell Oman is fully 
            committed to adopting the best global practices of corporate 
            governance and fully supports the guidelines on corporate governance 
            enunciated by the Capital Market Authority. Being part of the global 
            conglomerate, Shell Oman has easy access to principles, policies, 
            procedures and practices for doing business and conducting affairs 
            that are commonly used in the Shell Group.<br>
            <br>
            Challenges &amp; Forecast: “Aggressive competitors!” points out Irshad 
            Lawati, Managing Director, without batting an eyelid. According to 
            him, when one strives to be a more long-term and 
            sustainability-oriented market maker, a short-term oriented, 
            competitive aggression could cause distraction in responding to. “We 
            believe there will be enough opportunities for all players in the 
            market but the challenge will continue to be the same, to make sure 
            that business development is forged in a value-led approach in some 
            parts of the market”.<br>
            <br>
            Global base oil supply is another threat. Another challenge is Shell 
            Oman’s inability or reluctance to pass on increased unit price of 
            some base oils to customers through price adjustments. The company 
            hopes that the global economic outlook will remain robust for the 
            short to medium term and the various projects commenced in the local 
            market environment will “last through 2006 and beyond”.<br>
            <br>
            Shell Oman’s application for several New To Industry (NTI) retail 
            stations are pending with the government. Consolidation of 
            convenience stores (Select) will be another thrust. Plans are afoot 
            to grow its lubricant market share from the present 41 per cent and 
            strengthen its market leader position. <br>
            <br>
            The “second wave of investment” in Oman, fuelled by excess liquidity 
            situation in the market, “will help build momentum for early 
            completion of mega projects,” Shell Oman predicts. Economic 
            indicators for 2006 are positive and point to 10 per cent GDP 
            growth. All these indicate better business climate for everyone, 
            including Shell Oman. Moderate growth in aviation is anticipated and 
            a big plan in the blending segment is also on the cards. <br>
            <br>
            What does Lawati expect from 2006? With his company’s ‘MTM strategy 
            (Making the Most of What We Have...) well in place, stakeholders are 
            promised the best. Lawati sees 2006 as “an year for growth and 
            opportunities”. What more one can ask for? </font></p>
