            <p align="justify" style="margin-left: 10px; margin-right: 10px">
            <font face="Verdana" style="font-size: 16pt"><b>
            CHALLENGING Times</b></font><font face="Verdana" size="1"><b><br>
            Oman Oil Marketing Company has slid to the 6th position from the 
            previous 4th, due to new situations emerging in the market, 
            including increased competition </b><br>
            <br>
            </font><font face="Verdana" style="font-size: 11pt; font-weight: 700">T</font><font face="Verdana" size="1">hrough 
            a slew of measures such as increased number of sites, better product 
            and customer mix coupled with higher selling price, Oman Oil 
            Marketing Company posted a remarkable performance in 2005. <br>
            <br>
            <b>
            <img border="0" src="omanoil1.jpg" width="150" height="189" align="left" hspace="5" vspace="2"></b>Despite 
            meeting all of its financial, safety and performance targets, the 
            company itself was relegated to the sixth position, according to our 
            research and survey. In 2004, Oman Oil was perched at Number 4. 
            Thanks to the listing of Omantel on the Muscat Securities Market, 
            and a superlative performance by Renaissance Services during the 
            period under review, Oman Oil has to contend with a lower position 
            on the OER Top 20. It is heartening to note that an oil and 
            gas-based economy is witnessing the emergence of a telecom giant and 
            a non-oil marketing major is occupying the high table in 2005.<br>
            <br>
            <b>
            Challenges</b><br>
            Oman Oil Chairman Salim Abdullah Al Rawas says “Oman Oil’s seventh 
            year as a public company has been challenging given the positive 
            growth in the economy coupled with our own growth initiatives. All 
            of these placed a strain on the supply situation which resulted in 
            stock outs at some of our sites particularly towards the end of the 
            year. Discussions are being held with the relevant authorities to 
            ensure that supplies are maintained at the required level going 
            forward. Despite the above, all areas of the business performed well 
            with increased volumes as well as maintaining/marginally improving 
            margins per litre in most businesses.” He further adds, “The Board 
            of Directors has also recommended a Stock split. The Board has 
            recommended that the nominal value of its shares be brought down to 
            100 baisas from the present One Rial. Upon approval by the 
            shareholders and the regulatory authorities, each shareholder will, 
            for every share of face value RO 1 held, get 10 shares with a face 
            value of 100 baisas each. The Board is of the view that this will 
            increase liquidity to the shares of the Company and consequently 
            enhance the shareholder value. This change is consistent with the 
            practice followed in other major capital markets around the world.”<br>
            <b><br>
            Bottom line</b><br>
            There were challenges all around: higher transportation cost, rising 
            working capital and the resultant interest costs. Yet, it is 
            remarkable that Oman Oil ended 2005 with its turnover rising by 27 
            per cent over 2004 and profit after tax shooting up by 39 per cent 
            during the same period under review. Though the Muscat Securities 
            Market dropped Oman Oil from the benchmark MSM 30 index, the share 
            price gained 61 per cent in 2005, taking into account the low/high 
            of RO4 in January and RO 6.45 in December. <br>
            <br>
            However, Oman Oil outperformed the MSM service sector index. With 
            the balance sheet remaining strong, thanks to continued focus on 
            debtors and cash management and the company’s commitment to 
            generating consistent profits and maximising the long-term value of 
            its shareholders, the payout was good: a cash dividend of 450 baizas 
            per share — 45 per cent of the ordinary paid up share capital. <br>
            <br>
            Business: Retail segment performed well, helping a 14 per cent 
            growth in volume terms. “Altogether, the level of activity is the 
            highest it has been in a number of years,” gloats Mike Wilson, the 
            outgoing Managing Director. Volume-wise, Smart Card Ejaba and Wayak 
            increased sales by 37 per cent in their category. Quick Shop concept 
            also pitched up with additional revenue. Basma, the first of its in 
            multi-partner loyalty programme in Oman (with BankMuscat and Lulu 
            Hyper Market), also contributed to the growth in turnover. <br>
            <br>
            As far as bulk fuels are concerned, the construction boom in Sohar 
            particularly helped Oman Oil. As tourism gains momentum, Oman Oil 
            anticipates its aviation fuel business will get a leg up there as 
            well. The two-year aviation fuel supply contract for Royal Airforce 
            of Oman at Musannah base, where heightened activity is forecast, 
            will be a positive growth factor for Oman Oil. Lubricant business 
            (Oman Oil distributes BP and Castrol brands) is under pressure due 
            to higher base oil price globally. <br>
            <br>
            Oman Oil boasts that maintaining the highest standards of corporate 
            governance is “not a matter of compliance but an inner belief; it is 
            a way of life and an integral part of the company’s core values”. 
            The statutory auditor KPMG vouches that all is well. A sound 
            internal control process is in place to ensure that Oman Oil 
            conducts its business dealings “ethically and legally”. Omanisation 
            process is in full swing, with the current level pegged at 80 per 
            cent. Infusing young blood in a systematic manner has helped Oman 
            Oil achieve an age reduction of 8 per cent during the year.<br>
            <b><br>
            Forecast</b><br>
            Oman Oil is nursing visions beyond the boundaries of the Sultanate 
            to maximise growth potential. Regional expansion opportunities are 
            being explored. Through a 50:50 JV partnership with Al Sarooj group, 
            the company has got into an engagement for fuel supply-related 
            equipment to Iraq. Simultaneously, another JV in Yemen with a local 
            partner for retail operations is on the discussion table. A 
            feasibility study on the Yemen retail network is being carried out 
            in association with Al Sarooj group. <br>
            <br>
            Retail operations will continue to be the growth driver in 2006. All 
            the spadework carried out in 2004 and 2005, will bear fruit in 2006, 
            hopes Wison. Rivals, beware!</font></p>
