                        <b>Lebanese-Omani Business Forum<br>
                        </b><br>
                        <img border="1" src="biz_brief3.jpg" align="right" width="150" height="101">Shangri-la 
                        Barr Al Jissah Resort &amp; Spa is going to host the 
                        Omani-Lebanese Business Forum on 3 and 4 June. The Forum 
                        aims to provide a conducive framework and platform for 
                        both Omani and Lebanese businessmen to meet and exchange 
                        investment opportunities in the various economic sectors 
                        with focus on tourism, trade, industry, transport, 
                        banking, finance, insurance as well as information 
                        technology and others, as well as triggering the 
                        creation of a network between businessmen, to help 
                        improve economic and investment cooperation on the 
                        private sector level. The Omani-Lebanese Business Forum 
                        will host a series of roundtables and panels that will 
                        include keynote speeches and interventions to be 
                        delivered by high-profile speakers and panelists from 
                        the Sultanate of Oman, Lebanon and other Arab countries. 
                        It will be a platform to provide hands on and insightful 
                        presentations and exchange views about major business 
                        and economic issues with focus on mutual cooperation and 
                        opportunities as well as the investment climates in both 
                        countries.</font></p>
