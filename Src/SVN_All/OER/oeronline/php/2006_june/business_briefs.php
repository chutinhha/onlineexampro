                        <b>Oracle moves to KOM<br>
                        </b><br>
                        Oracle Corporation, the world’s largest enterprise 
                        software company, has signed up as a new tenant of 
                        Knowledge Oasis Muscat (KOM). Gabriel Bakhazi, Vice 
                        President, Finance, Oracle Middle East &amp; Africa, said: 
                        “Oracle is growing and developing as a company and KOM 
                        very much fits our profile and aspirations. We needed 
                        more office space and KOM is ideal in terms of 
                        facilities and easy access to other locations, besides 
                        offering a modern and beautiful working environment”.
                        <br>
                        <br>
                        “It’s great to see KOM welcoming IT giants such as 
                        Oracle — the Oracle team will be amongst the first to 
                        take advantage of the superb new facilities being built 
                        on KOM,” commented Dave Pender, Advisor at PEIE. Other 
                        recent high profile moves to KOM include Hewlett Packard 
                        and Huawei. </font></p>
