                        <b>The Wave sales nets over $100m<br>
                        </b><br>
                        The Wave, Oman’s upcoming tourism and beachfront 
                        residential project, has announced that its first 
                        residential release has evoked an unprecedented response 
                        with sales touching over US$100 million. Half of the 
                        units in the first residential release, covering 221 
                        townhouses and villas and expected to be completed in 
                        early 2008, were bought by Omanis with the remaining 
                        bought by the expatriates mostly based in the Sultanate, 
                        according to company officials. The first release 
                        witnessed buyers from 24 different nationalities. The 
                        construction of units in the first release is expected 
                        to coincide with the completion of the Oman’s first sea 
                        facing Golf Course — the signature course to be designed 
                        by renowned golfer Greg Norman. <br>
                        <br>
                        The Wave, Muscat is the first mixed use development 
                        granted the right to sell freehold property rights to 
                        all buyers, including foreigners. Eng. Abdullah bin 
                        Abbas bin Ahmed, President of Muscat Municipality and 
                        Chairman of The Wave, Muscat said: “The unprecedented 
                        demand for the first release underpins the uniqueness of 
                        our development. While we were always confident about 
                        the success and the interest it would generate, to 
                        exceed sales of US$100 million reflects the faith 
                        everyone has in our project and its iconic nature. </font></p>
