                        <p align="justify" style="margin-left: 5px; margin-right: 5px"><font face="Verdana" size="1">
                        <b>Gulf Air sponsors Oman’s football team<br>
                        </b><br>
                        Oman Football Association’s (OFA) signed its first-ever 
                        corporate sponsorship deal with Gulf Air. As per the 
                        multi-million dollar sponsorship deal, the airline will 
                        be the official sponsor for the national senior, 
                        Olympic, Under-19 and Under-17 teams. “As part of our 
                        smart airline, successful business strategy, we are 
                        already helping to build a better future for Oman by way 
                        of our financial contribution to the country’s economy,” 
                        said Gulf Air President and Chief Executive, James 
                        Hogan. <br>
                        <br>
                        “This has been achieved by increased flights through 
                        Seeb Airport and initiatives such as the new maintenance 
                        centre and the worldwide call centre in Muscat. Now we 
                        want to turn to more leisurely pursuits and build 
                        something that is even closer to the heart of most 
                        Omanis — the national football teams.” Hogan added that 
                        football is the national sport of Oman. “What better way 
                        to fulfill one’s commitment as the national airline than 
                        to put significant financial and logistical support 
                        behind the OFA? We hope that we can help the senior team 
                        climb the world rankings and at the same time foster 
                        exciting new footballing talent across the Sultanate.” 
                        Hogan and OFA Chairman, Khalil bin Ahmed Al Khalili 
                        signed the three-year sponsorship deal at a gala lunch 
                        at the Al Bustan Palace Hotel graced by a number of 
                        dignitaries, including Oman’s Minister of Sports 
                        Affairs, HE Ali bin Masoud bin Ali Al Sunaidi.<br>
                        <br>
                        The three-year sponsorship will give Gulf Air multiple 
                        branding, promotional and sales opportunities, including 
                        naming rights, team attire, tournaments signage, trade 
                        display, spectator entrance branding, after game 
                        ceremonies, VIP hospitality, other printed materials, 
                        promotional activities, travel commitments, player 
                        promotions, promotional materials and all media and 
                        advertising activities. This announcement is the latest 
                        in a number of high profile sponsorships by Gulf Air in 
                        the sultanate, including the Muscat Festival, Rally Oman 
                        and the Omani Song Festival.</font></p>
