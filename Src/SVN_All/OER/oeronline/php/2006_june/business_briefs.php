                        <b>Oman Awards for Excellence<br>
                        <br>
                        </b>
                        <img border="1" src="biz_brief1.jpg" align="right" width="150" height="99"></font><b><font face="Verdana" size="4">M</font></b><font face="Verdana" size="1">atrah 
                        Cold Stores picked up the ‘Flame of Excellence’ award 
                        for the year 2005 at the eighth annual Oman Awards for 
                        Excellence held recently. The award was sponsored by 
                        BankMuscat. Oman Cables Industry, Oman Textile Holding 
                        Company, Sun Packaging and Taageer Finance were the 
                        other finalists for the award. Among other categories, 
                        Ministry of Manpower and Ministry of Sports Affairs 
