                        <p align="justify" style="margin-left: 5px; margin-right: 5px"><font face="Verdana" size="1">
                        <b>Oman to buy vessels for US$66 million<br>
                        </b><br>
                        The Sultanate of Oman has signed a contract agreement 
                        with Australian shipbuilder Austal for two multi-purpose 
                        passenger-vehicle ferries to connect the northern 
                        coastal areas. The agreement was initialed by HE Ahmed 
                        bin Abdulnabi Macki, Minister of National Economy and 
                        the Australian company’s Board Chairman John Rothwell. 
                        Macki said that the agreement was within the framework 
                        of the state’s efforts to develop coastal communities 
                        and densely populated areas. The vessels would transport 
                        passengers as well as goods and help provide services 
                        for the targeted areas. Their delivery is expected by 
                        2007 or early 2008.</font></p>
