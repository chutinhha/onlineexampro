                        Oman Cables Industry was the recipient of ‘Exporter of 
                        the Year’ award sponsored by Oman’s Export Credit 
                        Guarantee Agency. AATCO and Al Jazeera Tube Mills were 
                        among the finalists. BankMuscat was successful in 
                        winning ‘People Development Award’ sponsored by The 
                        Week. Safeer Group of Hotels and Taageer Finance were 
                        also in contention. Introduced for the first time, 
                        ‘Environmental Award’ went to Petroleum Development 
                        Oman. It was sponsored by Sohar Aluminum.</font></p>
