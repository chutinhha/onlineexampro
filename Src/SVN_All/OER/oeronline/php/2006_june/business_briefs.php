                        <b>OCIPED launches ProductMap<br>
                        </b><br>
                        OCIPED, in association with International Trade Centre, 
                        Geneva (ITC), has established Oman ProductMap — a trade 
                        information web portal covering 72 sectors, which can be 
                        compared to a virtual library. Faris Al Farsi, Director 
                        of Facilitation in Export Marketing of OCIPED informed 
                        that Oman ProductMap is a very important commercial 
                        tool, as it allows for a more thorough understanding of 
                        the international marketplace. “It encompasses 
                        sector-related trade statistics, macroeconomic 
                        indicators, links to key information sources for 
                        exporters, market studies, pricing reports for cut 
                        flowers, spices, pharmaceutical materials, fruit juices 
                        and fruit and vegetables, networking tools such as 
                        business contacts and ‘storefronts’ (basic one-page web 
                        site, hosted on P-Map server, presenting the company and 
                        the leading products),” he said.</font></p>
