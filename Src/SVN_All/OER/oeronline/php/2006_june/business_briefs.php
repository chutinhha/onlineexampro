                        <b>Travco to develop RO20 million resort <br>
                        </b><br>
                        The Dubai based tour operator, Travco Group, has inked a 
                        memorandum of understanding (MoU) with Oman’s government 
                        to develop a RO20 million resort hotel in the Sultanate. 
                        The MoU was signed by the CEO of Travco Group Holdings, 
                        Hamed Al Chiaty, and Oman’s Minister of Tourism, HE Dr 
                        Rajiha Abdul Ameer Ali. Osama Bushra, Travco’s Regional 
                        General Manager informed that the company is setting up 
                        an office in Oman to manage the project’s development 
                        and management of the property. He added: “With this, we 
                        would like to be a part of the Sultanate’s booming 
                        tourism industry by not only representing Oman as a 
                        significant destination, we will also be involved in 
                        greater projects such as constructing and managing 
                        hotels and resorts in the country.” Travco is part of 
                        the leading German tour operator TUI Group.</font></p>
