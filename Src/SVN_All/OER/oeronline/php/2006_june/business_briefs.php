                        <p align="justify" style="margin-left: 5px; margin-right: 5px"><font face="Verdana" size="1">
                        <b>Omantel inks MPLS deal with CBO <br>
                        </b><br>
                        <img border="1" src="biz_brief2.jpg" align="left" width="150" height="100">Oman 
                        Telecommunications Company (Omantel) has signed an 
                        agreement with the Central Bank of Oman for the 
                        establishment of Multi-Protocol Labeling Switching (MPLS) 
                        for advanced computer technology and a communications 
                        super-highway (BankNet). This is touted as the most 
                        up-to-date and fastest system in electronic 
                        telecommunications technology. It will lead to a 
                        comprehensive change in the way money flows among the 
                        banks operating in the Sultanate. <br>
                        <br>
                        Omantel is expected to gross an annual revenue in excess 
                        of RO162,000 from this project which is a strategic 
                        undertaking for the banking sector as a whole as it will 
                        develop all banking operations. The agreement was signed 
                        by HE Hamoud Bin Sangour Al-Zadjali, the Executive 
                        President of the Central Bank of Oman and Dr. Mohammed 
                        Bin Ali Al-Wohaibi, the Executive President of Omantel. 
                        To develop the Omani banking system and raise its 
                        international standards, CBO started implementation of 
                        its ambitious project of the Electronic Funds Transfer 
                        “EFT” in 2005. This new system is expected to 
                        revolutionise dealings among banks. CBO has selected 
                        Omantel as strategic partners to provide the necessary 
                        infrastructure, design, implement and run the system.
                        <br>
                        <br>
                        HE Hamoud said the Electronic Funds Transfer consisted 
                        of several components, including the Real Time Cross 
                        Settlement (RGTS) among banks and the Automatic Clearing 
                        House (ACH), the Electronic Cheques Clearing “ECC” 
                        through cheque photocopying, Point of Sale system (POS), 
                        Delivery versus Payment (DvP) and Payment versus Payment 
                        (PvP). Dr. Mohammed added that as this is a highly 
                        advanced project, Omantel adopted the MPLS through the 
                        BankNet, which guarantees the safety and confidentiality 
                        of dealings. To this effect, Omantel shall provide CBO 
                        with Internet Protocol “IP” through the BankNet without 
                        any need for extra codes. Omantel has entrusted the 
                        network management task to NCR Company for two years.</font></p>
