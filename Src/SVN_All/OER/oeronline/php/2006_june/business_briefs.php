                        <p align="justify" style="margin-left: 5px; margin-right: 5px"><font face="Verdana" size="1">
                        <b>Suez Energy powers Oman<br>
                        </b><br>
                        Suez Energy International successfully started two 
                        projects in the Middle East. In May, Suez Energy 
                        International, a division of Suez, succeeded in 
                        delivering early power for Sohar in Oman and Al Ezzel in 
                        Bahrain. The two projects acquired in mid 2004 were 
                        developed on a tight construction schedule, with works 
                        starting after achieving financial close in November 
                        2004 and a first operational phase ready by end of April 
                        2006. The Sohar Independent Power and Water project 
                        consists of a 585 MW combined cycle, turbine plant and a 
                        33 million imperial gallons a day (6125 cubic meters per 
                        hour) water desalination plant. Since April, the plant 
                        is already providing 360 MW of power to meet <br>
                        the growing energy demand in Oman.</font></p>
