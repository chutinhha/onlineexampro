                        sponsored by OCIPED. Muscat Securities Market was also a 
                        finalist for the category. Zahara Tours got ‘Tourism 
                        Award’ (sponsored by Oman Air), beating other finalists 
                        that included Muscat Diving and Adventure Centre and 
                        Safeer Group of Hotels. Oman International Trade and 
                        Exhibitions received the ‘Entrepreneur of the Year’ 
                        award (sponsored by Omantel), beating the other finalist 
                        Sadaf Trading Enterprise. <br>
