<b>Scramble for Sohar airport consultancy</b></font><font face="Verdana" size="1"><b><br>
With the Omani government inviting bids for a feasibility study on the planned 
airport in Sohar, as many as 20 international consultants have entered the fray 
to garner the deal<br>
</b><br>
More than 20 leading consultancy firms are in the race for the contract linked 
to the construction of the Sohar Airport in the Sultanate. The ministry’s 
contract for the provision of consultancy services covers the pre-design study, 
preliminary and detailed design drawings, tender document preparation, tendering 
and post-tendering services, construction supervision and estimation of costs 
for the construction of the Sohar Airport.<br>
<br>
Competing for the consultancy services contract are well-known firms such as 
Khatib and Alami; Dar al Handasah; Cowi and Partners; ADP I Architects, 
Engineers and Planners; National Engineering Office, The Louis Berger Group; 
Consulting Engineering Services; Gulf Engineering Consultants; Griffin and 
Partners; Al Hatmy Engineering; HA-M; Larsen Architects; Parsons; Technital 
(Qatar); Al Manarah Engineering; W&amp;P Architects; Renardet; Bechtel Limited; 
INECO (Spain); Hanscomb and Co; and Netherlands Airports Consultants.<br>
<br>
A number of local and international companies have evinced interest in a 
consultancy services contract linked to the design and construction of an 
international-scale airport planned in the Wilayat of Sohar. Envisaged at Sohar 
is a facility large enough to handle some of the biggest passenger aircraft in 
operation today or due to enter service in the coming years.<br>
<br>
Oman has invited bids from international consultants for a feasibility study on 
the planned airport in Sohar. In January this year, the Oman government 
announced its plan to build three new airports: at Sohar in the north, in the 
southern city of Duqum and at Ras al Hadd, a tourist destination. Sohar is one 
of the Gulf’s fastest developing industrial zones, witnessing mammoth 
investments, especially in gas-based industrial ventures.<br>
<b><br>
Airbus vs Boeing</b><br>
The airport will be designed to accommodate aircraft as large as the Boeing-747, 
as well as the world’s largest airliner, the Airbus A380 super-jumbo, which will 
enter service in 2007. The A380 can seat more than 800 passengers across twin 
decks. Development of the airport, which is being overseen by the Ministry of 
Transport and Communications, is part of a wider effort to expand transport 
infrastructure in and out of the wilayat in tandem with the development of a 
huge industrial port and petrochemicals hub at Sohar.<br>
<br>
Besides meeting world-scale standards in its design and construction, the 
proposed airport will be designed for a capacity of 500,000 passengers per year. 
The design standards of the facility will be in keeping with IATA Class-A 
standards, as well as the recommendations and standards of the International 
Civil Aviation Organisation (ICAO).<br>
<br>
The selected design consultant will be required to prepare a detailed master 
plan layout, charting the future growth of the airport and all associated 
facilities over the next 30 years. The master plan layout will include features 
such as the paved runway, taxiway and aprons, passenger terminal building, air 
traffic control tower (ATC), rescue and fire-fighting services, catering 
building, fuel facilities, and security facilities.<br>
<br>
Also envisaged as part of the project is a cargo building designed for a 
capacity of 100,000 tonnes of air cargo per year. Also, as part of the 
pre-design phase, the selected consultant will be required to study the 
possibility of shifting the runway alignment and orientations, proposed in the 
existing conceptual study developed around eight years ago. <br>
Besides taking into account the additional land made available for the project, 
the pre-design study is aimed at ensuring that the airport facilities are not 
affected by existing constraints, such as aflaj systems, gas pipelines and other 
obstacles that may have developed in the last eight years within or outside the 
proposed airport site.<br>
—Sowmya Sri Rallabhandi</font></p>
<p align="justify" dir="ltr" style="margin-right: 10px">
<font face="Verdana" style="font-size: 16pt">
<b>RO22 million water facility goes on stream</b></font><font face="Verdana" size="1"><br>
<b>Majis Industrial Services Company SAOG to undertake the management of the 
newly opened facility</b><br>
<br>
</font><font face="Verdana" style="font-size: 11pt">
<b>S</b></font><font face="Verdana" size="1">ensing the growing importance of 
salt-free water for industrial purposes in the Port of Sohar, the government has 
made a bold move to establish a new company to manage the Seawater Intake and 
Return System (SWIRS). Majis Industrial Services Company SAOG (MISC), a wholly 
owned government firm, will undertake the management of the newly opened 
facility and develop other water-related services in the Sohar Industrial Port 
Area.<br>
<br>
SWIRS will meet the cooling water needs of six offtakers by pumping water 
directly through their own installation (pumps and pipelines). The six offtakers 
are Sohar Refinery Company (along with Oman Polypropylene), Oman International 
Urea and Chemical Industries, Oman Methanol Company, Sohar Power Company, Sohar 
Aluminium, and Shadeed Steel.<br>
<br>
The system will also provide cooling water to individual smaller users by way of 
a common distribution network. These smaller users are Ethylene Dichloride 
Project, Sharq Sohar Steel Rolling Mills, Aromatics Oman, Oiltanking Odfjell 
Terminals Oman, and a planned Special Economic Zone (SEZ). Oman Petrochemical 
Industries, a joint venture of Oman Oil Company and Dow Chemical, will be 
supplied with cooling water from a second intake station planned at the port.<br>
<br>
SWIRS’s design allows for smooth flow of seawater through a dredged channel 
built between two breakwaters of a total length of 1,800 metres, stretching 600 
metres into the sea. An oil boom barrier placed at the mouth of the intake 
prevents the ingress of any spilled oil. Measures have also been taken to 
prevent attacks from hundreds of tonnes of jellyfish that can potentially clog 
the screens and mechanical parts of the SWIRS, leading to a total shutdown of 
the pumping station, as well as the industries.<br>
<b><br>
Solid Backing</b><br>
The seawater intake system, built at a cost of RO22 million, is the largest of 
its kind in the Sultanate. The Ministry of National Economy has financed the 
construction of the facility, which has been placed under the administrative 
purview of the Public Establishment for Industrial Estates (PEIE). Construction 
was undertaken by a consortium of Bahwan Contracting Company (BCC) and Sezai 
Turkes Feyzi Akkaya Construction Co (STFA) of Turkey.<br>
<br>
While BCC carried out the onshore works, STFA executed the offshore component. 
Operation and maintenance is currently being handled by well-known Omani 
engineering firm, Bahwan Engineering Company (BEC), under a separate four-year 
contract awarded last year. When operating at full capacity, the Seawater Intake 
System will make available seawater at the maximum rate of 334,000 cubic metres 
per hour to meet the cooling water requirements of around 17 industrial plants 
located around the Port of Sohar. <br>
<br>
At the heart of the system is a reinforced concrete-framed pumping station where 
each of the planned industries will install their own pumps to extract seawater 
and pump the same through their own mains. The station is fully equipped to 
screen and chlorinate the seawater before it is made available to the industries 
to extract. Also included in the works is a power substation capable of 
supplying power for the pumping station as well as for the seawater intake pumps 
to be installed by the industries.<br>
<br>
After utilising the seawater for cooling and other purposes, the industries will 
deliver the return seawater to a common outflow system for discharging it back 
into the sea. The discharge of seawater will be undertaken as per environmental 
criteria, and at stipulated temperatures that will not harm the marine 
environment. Additional measures, such as oil barriers and anti-sediment 
screens, are also in place at the pumping station to prevent entry of any 
material into the intake system.<br>
<b><br>
First off the block</b><br>
Sohar Refinery Company, whose world-scale refinery plant is currently in the 
commissioning phase, was the first to install pumps last September in order to 
draw its requirements of cooling water. Sohar Power Company, which is developing 
a major power and desalination plant at Sohar, has also since followed suit.<br>
Authorities have also initiated the process of augmenting the industrial port’s 
seawater intake capacity to serve projects that were finalised long after the 
present intake system was conceived and designed. A contract is expected to be 
awarded in the coming months for the design of the second phase of the seawater 
intake and return system. The capacity of the intake channel has already been 
widened to handle 700,000 cubic metres of water per hour, to cater for future 
cooling water requirements.—Sowmya Sri Rallabhandi</font></p>
