<p align="justify" dir="ltr"><b><font face="Verdana" style="font-size: 16pt">
<br>
</font><font face="Verdana" size="5">Selling the KwaZulu dream</font><font face="Verdana" size="6"><br>
</font></b><font face="Verdana" size="1"><b><br>
Dr. Zweli Mkhize, Minister of Finance &amp; Economic Development, the KwaZulu-Natal 
Provincial Government (South Africa), explains to OER how his province is an 
ideal destination for Omani investors </b><br>
<br>
</font><font face="Verdana" style="font-size: 11pt"><b>
<img border="1" src="passing_by.jpg" align="left" width="100" height="110">O</b></font><font face="Verdana" size="1">f 
late, a young crop of South African leaders has taken it upon itself to sell the 
country as a dream destination for investment. After around a decade of the 
democratic experience, and tiding over the initial hiccups, South Africa’s 
economy is on a roll. Credit for that should also go to these young leaders. 
Suave and articulate, they come across as the perfect brand ambassadors to have 
happened to South Africa at this point of time in their country’s history. Dr. 
Zweli Mkhize belongs to this very brigade. <br>
<br>
The Minister of Finance &amp; Economic Development in the KwaZulu-Natal Provincial 
Government, South Africa, Dr. Mkhize, in May, led a 50-member South African 
delegation to Oman on a trade and investment mission. For Dr. Mkhize, the trip 
to Oman has been a revelation. “It’s literally like discovering each other,” he 
says. <br>
<br>
“We are also looking at investment opportunities for South African companies to 
invest in Oman. We are also looking at areas where we can build trade links,” 
Dr. Mkhize says. The KwaZulu dream that he is selling right now, hinges on some 
unique advantages that the South African province enjoys. <br>
<br>
Dr. Mkhize’s province, KwaZulu-Natal (KZN), is South Africa’s second largest 
economy and generates 20 per cent of the country’s GDP. Home to two of Africa’s 
busiest ports and a well-developed road and rail transport system, KZN can be 
seen as an international gateway to the southern Africa.<br>
<br>
Dr. Mkhize says his visit to the Sultanate entails not only introducing a 
different part of his country but also to reiterate the relationship between 
South Africa and Oman. “We are focusing on inviting Omani tourists to my 
province and country. We are looking for the market that South African products 
have in Oman and vice versa.”<br>
<br>
We believe that the platform has already been set for taking the Oman-South 
Africa trade ties to greater heights.” <br>
<br>
<b>Untapped Potential </b><br>
Dr. Mkhize feels that the kind of similarities that the Oman and South Africa 
share, the two countries should be having strong trade ties for a long time now. 
He concedes that the potential, in this regard, has perhaps not been fully 
tapped. “We need to work on this. But we are really encouraged to see that some 
South African businesses are coming up in Oman. Still, I think they are too 
few.” <br>
<b><br>
Key Business Opportunities</b><br>
Better late than never. The delegation led by Dr. Mkhize is aware that a late 
beginning may not be a dampener if the right efforts are made in earnest to tap 
the untapped potential. Some of the key areas identified by the team for 
exploring business opportunities include tourism, agro-processing, oil and gas, 
real estate, maritime, human capital development, development finance, 
furniture, and equine trading. <br>
<br>
Dr. Mkhize feels Oman can make use of his province’s rich resources in 
agriculture, iron and steel, technology and tourism. KZN province, he says, has 
the potential to produce virtually everything that Oman requires in the food 
industry. <br>
<br>
“We are here to make Omani businessmen aware of this huge potential and explore 
possibilities of partnerships and joint ventures to produce, process or export 
meat, dairy products, wool, eggs, hides, skins, and field crops like sugarcane, 
maize, wheat, cotton, oil seeds and fodder,” points out Dr. Mkhize.<br>
<b><br>
Attraction for Omani Investors</b><br>
What are the unique advantages for an Omani investing in KZN? According to Dr. 
Mkhize, his province’s strategic location is one big plus, which makes the 
province a logical partner for trade. Then, for Omani businessmen looking to 
enter Africa, this province provides unique advantage. <br>
<br>
What about taxes? Dr. Mkhize concedes that unlike most of the Gulf countries, 
they do charge taxes, but they have a stable system that easily withstands 
change at the political level. He says his country as well as the province is on 
the verge of real economic boom. South Africa has had sustainable growth over 
the past 10 years as a new democracy. And it is targeting an economic growth of 
6 per cent between now and 2014. “We think it is within our reach to achieve 
this,” he asserts. <br>
<br>
The other area that can be tapped, according to Dr. Mkhize, is education. “We 
have very good academic institutions and we would like Omani people to take 
advantage of these,” he says. South African people can come to Oman and work 
here. The work ethic in South Africa, he points out, is extremely good. So that 
is a big plus for countries that hire this workforce.<br>
<b><br>
Oman as Investment Destination</b><br>
Dr. Mkhize believes Oman has great investment opportunities. He refers to the 
“highly enthusiastic” Omani business community, which is very keen to learn and 
explore about what’s going on in South Africa. <br>
“Omani people are very hospitable,” he says. In fact, he felt overwhelmed by the 
reception his delegation received from the Omani business community. He confirms 
a plethora of business enquiries from Omanis. “If we can work on this, trade 
ties between the countries are sure to reach dizzy heights,” he asserts.<br>
Shifting focus to the specifics, the KZN minister sees tremendous business 
opportunities in the Sohar industrial port, tourism, infrastructure development 
and real estate.<br>
<b><br>
Points of Convergence</b><br>
Like Oman, points out Dr. Mkhize, where a huge investment is being made to 
develop a new port, KZN has a port too. “We can look at exchange of technology 
in terms of logistics and issues concerning port management. We can also look at 
chalking out a relationship in terms of movement of goods from Sohar to Durban 
and vice versa. We think this is a very important area, as Sohar is not very far 
from Dubai, Abu Dhabi or Qatar. So you can look at it as a very strategic 
location,” he emphasises. <br>
<br>
There can be exchange of students as well. Oman can send students for training 
to South Africa. “We are also exploring the possibility of training of staff, 
especially in the hospitality and tourism-promotion industry,” he reveals. <br>
South Africa has the best water. The water bottling industry is booming in this 
African country. Dr. Mkhize believes this is another area where something can be 
done. <br>
<br>
Besides, he says, South Africa needs to be exposed to different types of people, 
areas and cultures. <br>
<b><br>
Tourism: Hot Spot</b><br>
To an Omani tourist wishing to fly to his province or the country, what are the 
main attractions? “It is not very far from Oman,” Dr. Mkhize quickly points out. 
And he may not be off the mark. It’s only 7-8 hour flight from Muscat. Gulf Air, 
Emirates, Qatar Airways, all fly to South Africa. In fact, it’s a much shorter 
destination compared to the other places that Omani people like to travel to.<br>
<br>
The minister also earmarks the culturally diverse community in South Africa as a 
unique selling point. “We have a very large Muslim community. We have several 
mosques in every big city. This makes it very comfortable for Muslims to visit 
South Africa. Halal food is available everywhere,” Dr. Mkhize points out. <br>
<br>
Among the other major attractions is the golfing facility. Every town in South 
Africa has golfing facility. Then, the stupendous climate! The coastal side has 
a very temperate, stable climate throughout the year. One can bathe at any time 
of the day/night. In the western highlands, you have mountains that are very 
cool and the rainfall is quite high. For those interested in hiking, these 
mountains are a fantastic experience. <br>
<br>
As one moves to the north, there are quite a few historical sites, like the 
Anglo-Zulu war site for example. (Zulu nation has a history of defeating 
colonial powers), informs the minister. One finds churches, mosques, Hindu 
temples...almost everything. Then, there are paintings carved on rocks that 
depict original African people who lived here, dating back to over 2,000 years. 
There are some amazing World Heritage sites in KZN.<br>
<br>
For an Omani, there is a lot to look forward to with regard to sports tourism as 
well. There is canoeing. There is horse race called July Handicap. Everyone in 
the area who has a horse wants to participate in this race. Then, there is the 
A-1 Grand Prix (watched by 122 million people across the world). What about the 
crime rate? Some believe this could be one deterrent. For, one often hears about 
stories of violence against women. Dr. Mkhize counters this. “The crime rate has 
gone significantly down in the post-apartheid era,” he says. <br>
<b><br>
Time Frame? </b><br>
Is there a time frame for the fruition of the visit? The KZN province minister 
is non-committal. Human nature is not like that, he points out. “People have to 
visit each other, introduce each other to friends and partners. And slowly, the 
whole family comes. <br>
<br>
We would want to work on the possibility of a high-profile delegation from Oman 
visiting South Africa. I would leave it to our Ambassador (to Oman). We would 
like to see other South Africans coming to Oman on follow-up visits.” <br>
<br>
The silver lining, according to this articulate visitor, is that the fundamental 
relationship is sound enough to allow the widest level of development of South 
Africa-Oman ties. And like it is said, well begun is half the job done!</font></p>

