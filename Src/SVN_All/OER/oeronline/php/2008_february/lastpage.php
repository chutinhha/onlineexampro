        <p align="justify"><b><font face="Verdana" size="4"><br>
        Telecom manager</font></b><font face="Verdana" size="1"><br>
        <b>Lars O Gustafsson, who took over as the Head-Marketing &amp; Sales 
        Division, Oman Mobile, four months ago, goes down memory lane to share 
        his experiences from trying out as a tennis player to a telecom manager 
        in a tête-à-tête with Akshay Bhatnagar </b><br>
        <br>
        <img border="0" src="lastpage.jpg" width="350" height="262" align="left" hspace="8" vspace="6">Life has been on a constant move for Lars O Gustafsson, who has today 
        spent more than two decades in the telecom industry in Europe and Latin 
        America. “Even though I’ve worked in different countries with different 
        cultures, one factor has remained constant. I’ve worked in markets in 
        early stages of telecom growth. When I moved to Oman to work in Omantel 
        three years ago, the Sultanate was in the early stages of mobile 
        services’ development. And now, we are driving growth of mobile services 
        across Oman as the Sultanate’s premier telecom player,” says Lars.<br>
        <br>
        Does it mean that he dislikes the cutthroat environment as seen in 
        matured markets? “Contrarily, it is equally challenging, if not more, to 
        build capabilities to serve customers, set-up the infrastructure and 
        bring new services to the market. It is not easy to set the ball rolling 
        and put effective processes in place to ensure a smooth flow of services 
        and business.” <br>
        <br>
        Even though the dynamics of the telecom market has always kept him on 
        his toes, he tries not to forget to give adequate attention to his 
        family. “One has to strike a fine balance between work and personal 
        life. The rigours of professional life should not take a toll on the 
        personal front. I take out my family (an HR professional wife and two 
        teenage daughters) on frequent outings. We visit Dubai almost every two 
        months. My daughters just love the city for its razzmatazz. With major 
        infrastructure developments taking place, we easily gets lost in Dubai, 
        as new roads or diversions come up every time we visit the city. In our 
        Toyota Prado, we have also seen the natural beauty of Oman and many 
        historical places.”<br>
        <br>
        Does he miss his home country? “Absolutely, although not so much the 
        dark winters. Sweden is always calling out and no wonder, we end up 
        going there 2 times in a year. With its long cold weather and nights, it 
        is a different place altogether. Talking of Europe, I love going to 
        Switzerland also. It is an amazingly beautiful country and no matter how 
        many times you have been there, it is never enough. I also enjoy 
        occasional visits to the musical clubs in England.”<br>
        <br>
        On his early years, Lars says, “The tennis legend, Bjorn Borg, given his 
        fantastic run at the Wimbledon, was an inspiration for most kids in 
        Sweden. I was no different than them. I always dreamt of following in 
        his footsteps. But in the course of life, it all fizzled out. In fact, 
        I’ve not played tennis for years now. I think as we move ahead in life, 
        many of our personal interests take a back seat. However, I’m a great 
        believer in keeping myself fit. I enjoy my walks on the pristine beaches 
        in Muscat.” Perhaps, that’s the secret of the fit physique and young 
        demeanour of the 46-year young Swede, a Leo, who is busy crafting a new 
        chapter in the big fight for mobile subscribers in Oman! </font></p></td>
