<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
</font><b><font face="Verdana" size="4">South Africa set to score</font></b><font face="Verdana" size="1"><br>
<b>In May 2004, FIFA awarded the hosting of the FIFA World Cup to an African 
country – the first time it has done so in the 101 years of its existence. HE 
Yacoob Abba Omar, South African Ambassador to Oman, explains the progress made 
and challenges being faced by the country to become the first African country to 
host the FIFA World Cup<br>
</b><br>
<img border="0" src="guest1.jpg" width="175" height="178" align="left" hspace="8" vspace="6">All of South Africa is eagerly looking forward to June 2010, when the pinnacle 
of the ‘beautiful game’ comes to the country. The hosting of the 2010 World Cup 
will be a catalyst for faster economic growth and the achievement of development 
goals. Public money is being invested in World Cup projects that will help 
achieve existing government objectives and development goals.<br>
<br>
From the beginning of the bid process, South Africa was committed that the 2010 
World Cup would be an African World Cup. Its efforts were strongly supported by 
the African Union (AU). In November 2006, the African Legacy Programme for the 
World Cup – a joint responsibility of the 2010 FIFA World Cup Organising 
Committee and the Government – was devised. <br>
<br>
South Africa, Zimbabwe, Zambia and Mozambique have signed agreements to share 
the tourism spin-offs of the World Cup through “border-free” packages. The 2010 
World Cup is also stimulating development in neighbouring countries. For 
instance, Mozambique will spend US$51 million on the rehabilitation of a railway 
line between Maputo and South Africa, and is also upgrading its road network. 
Mozambique is investing US$600 million on new hotels, casinos and other leisure 
facilities.<br>
<br>
The South African Government investment and programmes for the World Cup are 
planned in such a way that they will accelerate delivery in existing priority 
areas. By 2010, South Africa will have, among other things, better sports 
facilities, a better public transport system and roads, and better 
telecommunications infrastructure.<br>
<br>
However, hosting the World Cup is also about building the country in ways that 
go beyond improving infrastructure. It is also about developing football and the 
youth, promoting a healthy lifestyle through sports and fostering pride in our 
country and the continent.<br>
<br>
The government’s almost US$3 billion direct investment in infrastructure for the 
World Cup is part of a much larger spending programme between 2006 and 2010. 
During that time, the Government will be investing more than R400 billion (about 
US$6 billion) on infrastructure – from rail-freight service and energy 
production, to communication, airports and other ports of entry.<br>
<br>
The highlights of this investment plan are:<br>
<br>
<b>Spending on infrastructure (US$)</b><br>
Transport and supporting infrastructure: 1.5bn. <br>
Stadium: 1.3bn.<br>
<br>
<b>Spending on non-infrastructure (US$)</b><br>
Sport and recreation: 0.5bn<br>
Arts and culture: 0.2bn<br>
Safety and security: 0.9bn<br>
Health: 0.4bn<br>
<br>
The full-year profit of Aveng, South Africa’s biggest construction company, more 
than doubled in 2007 because of accelerating economic growth and building work 
on infrastructure needed to host the 2010 World Cup.<br>
<br>
The World Cup is stimulating multi billion-rand hotel developments across South 
Africa. This includes investments from large foreign companies such as Dubai 
World, InterContinental Hotels Groups and Starwood Hotels and Resorts Worldwide.<br>
<br>
The 2010 tournament will be the first World Cup during which FIFA will make use 
of non-hotel accommodation, providing business opportunities for guest houses 
and bed-and breakfast facilities. To benefit, small, medium and 
micro-enterprises (SMMEs) must register with Match (the company contracted by 
FIFA). SMMEs also have to be graded. The Department of Environmental Affairs and 
Tourism has made about US$3 billion available to assist with the grading of SMME 
accommodation.<br>
<br>
The Department of Environmental Affairs and Tourism is leading the process of 
“greening” 2010. This includes waste management, water and energy conservation, 
sustainable architecture in stadium, carbon offset and public awareness 
campaigns.<br>
<br>
South African tourism grew by almost 14 per cent in 2006, three times the global 
tourism growth rate of 4.5 per cent for the same period. Almost 8.4 million 
tourists visited South Africa in 2006 – over a million more than in 2005. This 
is expected to shoot up over the next two years leading to June 2010.<br>
<br>
The 2010 World Cup is already the most successful FIFA World Cup ever – it has 
generated US$3.2 billion in signed agreements, more than any other World Cup.<br>
<br>
South Africa’s entire transport infrastructure is enjoying a complete overhaul. 
Highlights include:<br>
<br>
In the 2007 Budget, the South African National Roads Agency and the South 
African Rail Commuter Corporation received an additional US$2.5 billion to 
upgrade roads and stations in areas critical to the World Cup.<br>
<br>
Transnet, the public entity responsible for South Africa’s rail transport, has 
embarked on an extensive investment initiative to recapitalise and privatise key 
aspects of rail transport. Other projects underway, such as the US$1 billion 
taxi recapitalisation programme and the Gautrain Rapid Rail Link in Gauteng, 
will tie in with transport demands likely in 2010.<br>
<br>
A fast comfortable and low-cost urban transport system, the “Bus Rapid Transit” 
system, is being developed for the host cities of the 2010 World Cup.<br>
<br>
Metrorail, South Africa’s biggest passenger rail commuter service-provider, is 
investing more than US$1 billion over the next three years to improve rail 
infrastructure. This includes 67 rail-related projects that have been identified 
for the 2010 World Cup.<br>
<br>
More than half-a-billion US dollars are being spent on upgrading Johannesburg’s 
Oliver Tambo International Airport and about US$250 million on the Cape Town 
International Airport. Similarly, the Airports Company South Africa (ASCA) will 
spend almost US$3 billion on infrastructure developments at its 10 airports 
until 2012.<br>
<br>
The games will be played in 10 stadiums spread throughout the country (see box). 
In order for more people to share in the excitement of the World Cup, South 
Africa will also have a number of fan parks– areas where giant screens are set 
up and air live telecasts of the matches. These were a huge success in the 2006 
World Cup in Germany.<br>
<br>
So Oman: make your bookings now, so that you can watch South Africa and Oman in 
the finals. </font>
