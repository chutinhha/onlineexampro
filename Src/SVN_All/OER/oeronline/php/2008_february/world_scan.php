</font><font size="2" color="#CC3300" face="Verdana"><b>CIA: Hackers break into 
power grid</b></font><font face="Verdana" size="1"><br>
<img border="0" src="worldscan.jpg" align="right" hspace="9" vspace="6">‘Hackers almost turned out the lights in multiple cities after breaking into 
electrical utilities and demanding extortion payments before disrupting the 
power’. This is not the theme of a new science fiction movie but a real life 
happening if we go by the claims of a senior CIA analyst. All the break-ins 
occurred outside the US, said Tom Donahue, while not identifying the affected 
countries, or when the outages occurred or how long the outages lasted. He said 
they happened in “several regions outside the US. In at least one case, the 
disruption caused a power outage affecting multiple cities,” Donahue said in a 
statement. “We do not know who executed these attacks or why, but all involved 
intrusions through the Internet.”<br>
<br>
There has been “a huge increase in focused attacks on our national 
infrastructure networks” in the past year-and-a-half, “and they have been coming 
from outside the US,” stated the noted cyber security expert. The coordinated 
attacks came from foreign governments or militaries, not terrorist groups, he 
said. According to media reports, in late November, the security firm McAfee 
singled out China for risking a “Cyber Cold War” by probing other countries’ 
Internet security, including a Pentagon network and other Western government 
agencies. A few days later, the British government warned major banks and other 
financial institutions that Chinese military hackers had targeted their 
networks.<br>
