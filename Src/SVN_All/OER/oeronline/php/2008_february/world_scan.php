<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
</font><font size="2" color="#CC3300" face="Verdana"><b>Google, Publicis join 
hands for digital ads</b></font><font face="Verdana" size="1"><br>
Advertising major Publicis Groupe and Google will jointly develop a unique 
approach to digital advertising that is both creative and technologically savvy. 
In a press statement, Maurice Levy of Publicis and Eric Schmidt of Google 
asserted that the collaboration gave them a “first-mover advantage” in combining 
the programming knowledge of Google with the creativity of advertising agencies. 
The resulting tools and business intelligence will not necessarily be 
proprietary to the two companies but can help propel the growth of digital 
advertising, they said.<br>
<br>
“It is possible that what emerges could drive the development of ‘open source 
advertising,’ if the tools were made widely available,” Schmidt said. Google, 
which makes much of its money by placing ads on particular web sites for other 
companies, promotes the adoption of common technical standards. Levy said 
current analytical methods were not enough to help agencies get to the right 
audiences on the Web, assess the effect of their ads and see a return on 
investment. The advertising on web sites and other digital media has been 
growing at a very fast pace compared to the traditional media such as television 
and print. This year, global online ad spending is expected to reach US$49.5 
billion, an increase of 22 per cent over last year, according to the research 
firm eMarketer.<br>
