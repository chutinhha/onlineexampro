<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
</font><font size="2" color="#CC3300" face="Verdana"><b>IMF: Economic slowdown 
unavoidable</b></font><font face="Verdana" size="1"><br>
<img border="0" src="worldscan1.jpg" width="204" height="136" align="right" hspace="8" vspace="7">The International Monetary Fund says “significant” slowing in the pace of global 
economic growth during 2008 appears inevitable. It also stated that restoring 
world financial markets was going to be a complex and protracted task.<br>
<br>
According to IMF spokesman, Masood Ahmed, the US Federal Reserve’s 75 basis 
point cut in the federal funds interest rate on January – the largest cut in the 
benchmark rate in more than 23 years – was “appropriate and helpful”. The Fed’s 
move followed deep losses on world stock markets as investors worried about a 
possible US recession and whether the slowing US economy would drag the rest of 
the world with it. “The volatile weakening experienced in many equity markets 
during the past few days has underscored the burden that the current financial 
market turmoil represents for the global growth outlook,” Ahmed said. “Thus, a 
significant 2008 slowing down in the global expansion already appears inevitable 
and downside risks still predominate.” <br>
<br>
In its last global economic outlook in October, the IMF said world growth was 
expected to ease to 4.8 per cent in 2008 and it recently revised its 2007 
estimate to 4.7 per cent after new data on China and India. On January 10, the 
IMF had said that while it does not predict a recession in the US, it cautioned 
that risks of one had increased. Still, the IMF has remained somewhat upbeat 
about global growth prospects mainly because of still robust growth in emerging 
economies.<br>
