<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
</font><font size="2" color="#CC3300" face="Verdana"><b>Thomson: Record M&amp;A 
failures in Asia-Pacific <br>
</b></font><font face="Verdana" size="1">The Asia-Pacific region, excluding 
Japan, experienced record levels of failed mergers and acquisitions in 2007, 
stated Thomson Financial. The figure was as high as US$69.5 billion from 295 
busted deals. Australia accounted for 62.5 per cent of all withdrawn deals in 
the region. Withdrawn M&amp;A transactions have been steadily on the increase, 
reaching US$65 billion for the first time in 2006, up from US$27.9 billion in 
2005 and US$25.1 billion in 2004. Thomson Financial said it had already recorded 
US$10.1 billion worth of withdrawn deals in 2008 from just eight bids. That is 
almost twice the US$5.8 billion in deals completed so far this year, the 
researchers said.<br>
<br>
The biggest withdrawn deal in 2007 was an US$11-billion buyout bid for 
Australia’s Qantas Airway. The bid lapsed after the consortium failed to get 50 
per cent of shareholder acceptance for their offer. Similarly, in 2006, the 
biggest failed deal was the $14-billion bid for Australian retailer Coles Meyer 
Ltd by a private equity consortium, led by Kohlberg Kravis Roberts &amp; Co KKR.UL. 
It failed after the Coles management rejected the consortium’s offer. So far, 
this year, the biggest busted deal has been the US$9 billion acquisition of 
Australia’s Orica. <br>
