we are focusing on includes improved Fuel Efficiency via advances in engine and 
transmission technology, E85 Ethanol, Hybrid Technology, Electric Technology and 
Fuel Cell Technology. This means that with the new technology that is in the 
pipeline, GM customers will be able to go from ‘Gas Friendly’ to ‘Gas Free’,” 
she continued. <br>
