<p align="justify" style="margin-left: 10px; margin-right: 10px"><font face="Verdana" size="1">
<br>
</font><b><font face="Verdana" size="4">
The 2008 budget focused on development, growth</font></b><font face="Verdana" size="1"><br>
<b>On January 1, 2008, HE Ahmad Bin Abdul Nabi Macki, Minister of National 
Economy and Deputy Chairman of the Financial Affairs and Energy and Resources 
Council, Oman, announced the state general budget for 2008. OER takes a look at 
what is in store</b><br>
<br>
<img border="0" src="news.jpg" width="175" height="169" align="left" hspace="8" vspace="6">Taken as a whole, the 2008 budget intends to augment growth rates. While doing 
so, it seeks to guarantee fairness in the distribution of economic growth in 
various regions as well as encourage foreign investment and economic 
diversification.|<br>
<br>
RO2,357 million has been earmarked for a range of development schemes. The total 
expenditure in 2007 was RO5,373 million, the Minister said. The policies have 
been framed specifically keeping in mind the need to meet the requirements of 
sectors such as health, education, harbours, gas, education, municipality 
services and city planning. The added funds, which record an increase of 78 per 
cent over the original estimate, will also be used for developing the natural 
gas sector, ports, roads and airports. The boost in revenue will be 
approximately 20 per cent (RO5.4 billion) and the rise in expenditure is 
estimated to be 19 per cent. The deficit, which is pegged at around 3 per cent 
of the Gross Domestic Product (GDP), will be funded from Government reserves. 
The GDP growth rate was estimated at 11.6 per cent in 2007, according to the 
Minister.<br>
<br>
There was an increase of 68.5 per cent in the cost of the development projects 
approved in the seventh five-year plan for ministries and civil governmental 
departments – it went up from RO3,016.3 million in the initial period of the 
plan to RO5,083 million by mid-November 2007, The gross expenditure in the draft 
budget for 2008 has gone up to RO5,800 million from RO4,890 in 2007, an increase 
of 20 per cent, the minister said.<br>
<br>
According to Mr Macki, the estimates for 2008 took into consideration the actual 
general expenditure during the past years, the outcome of committing to budget 
allocations and standards and use of the available database on the state budget. 
Development expenditure will reach RO725 million, an increase of RO225 million, 
and a 45 per cent growth compared with 2007 to cover the expenditure for ongoing 
development projects forwarded from the sixth five-year plan, besides new 
allocations and expenditure on new projects in the seventh five-year plan.<br>
<br>
<b>Oil revenues</b><br>
The Minister also pointed out that oil revenues were calculated at US$45 a 
barrel at an average of 790,000 barrels a day production, a jump of 8 per cent 
over the 2007 rate. The gross public revenue will touch RO5,400 million and as a 
result the budget deficit will be around RO400 million.<br>
<br>
The production of oil, which in recent years had been progressively declining, 
is budgeted to swell to 790,000 bpd. The 2007 budget estimate was 730,000 bpd. 
The seventh five-year plan is based on an average oil production of 827,000 bpd. 
This confirms that the Government’s efforts to improve oil production are 
finally paying off.<br>
<br>
Oil and gas incomes together comprise 78 per cent of the total revenues, while 
current and capital revenues account for the balance. Income from oil will rake 
in about RO3.6 billion this year, or 67 per cent of the total. Gas will bring 
RO620 million, 11 per cent of total revenues. Current and capital revenues are 
estimated to reach about RO1.2 billion, up 26 per cent over 2007. The total 
estimated expenditure of RO5.8 billion is 19 per cent or RO910 million more than 
last year. Current civil expenses are projected at about RO1.9 billion, an 
increase of RO305 million over last years estimates. Oil and gas production 
costs will claim RO1.3 billion, representing 23 per cent of the total and an 
increase of 12 per cent over 2007.<br>
<br>
Allocations for defence and security have been raised to RO1.4 billion from 
RO1.24 billion. The Government has budgeted RO1,360 million for defence and 
security, which accounts for 23 per cent of the total expenditure. This has 
increased by approximately 10 per cent as compared to 2007.</font></p>
<p align="justify" style="margin-left: 10px; margin-right: 10px">
<img border="0" src="news1.jpg" width="480" height="369"><font face="Verdana" size="1"><br>
<br>
<b>Private participation</b><br>
The Government has allocated RO385 million for participation and subsidies to 
the private sector, which includes a subsidy of RO118 million for the 
electricity sector. The Government’s allocation of RO249 million for 
participations in domestic, regional and international institutions include 
financing projects implemented by Oman Wastewater Company, Salalah Sanitary 
Drainage Service Company, Oman Oil Company, Oman Tourism Development Company, 
Majis Industrial Services Company and Salalah Free Zone Company.<br>
<br>
The share of the health and the educational sectors amounts to 49 per cent of 
the total current expenditures. Education has been allocated RO710 million, a 
hike of RO101 million over 2007, and health RO228 million, an increase of RO29 
million.<br>
<br>
RO30 million has been set aside for financial assistance to 48,499 cases covered 
by the social security system. High oil prices ensured that, instead of a 
deficit, Oman wound up 2007 with a substantial surplus, estimated at RO1.7 
billion. “This surplus will be utilised to strengthen the financial reserves of 
the government, as well as paying up some of the actuarial dues of the 
retirement funds,” the minister said. The price of oil was expected continue its 
upswing this year, he added.<br>
<br>
The Government has budgeted RO435 million as revenues from taxes and fees. 
Revenue from income taxes are projected to go up by 26 per cent from RO132 
million in 2007 to RO166 million in 2008. Revenue from customs duty is budgeted 
at RO100 million and fees from issue of non-Omani work permits are budgeted at 
RO86 million. The Government has also brought in a 35 per cent increase in both 
fees on real estate transactions and fees for hotels and other facilities.<br>
<br>
“One of the positive indicators in the budget for 2008 is the decline in the 
percentage of the deficit from the total revenues and from GDP,” he added. He 
said the new budget reflected “the government’s commitment towards building a 
modern national economy with competitive capabilities that can go along with the 
current fast transformations taking place in the international arena.” </font></p><div align="center">
