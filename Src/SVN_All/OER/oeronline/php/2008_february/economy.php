<p align="justify" style="margin-left: 10px; margin-right: 10px"><font face="Verdana" size="1">
<br>
</font><b><font face="Verdana" size="4">Oman looks east<br>
</font></b><font face="Verdana" size="1"><b>Oman’s ‘Look Asia’ policy has 
already borne early fruit, and more than oil is being discussed in the recent 
spate of high-level bilateral visits<br>
</b><br>
<img border="0" src="economy.jpg" width="138" height="420" align="left" hspace="8" vspace="6">A 
number of recent high-level visits demonstrate Oman’s growing closeness to the 
rapidly developing economies of Asia. Pranab Mukherjee, the External Affairs 
Minister of India, was in Oman in January discussing areas of potential 
cooperation with Deputy Prime Minister Sayyid Fahd Bin Mahmoud Al Said. The 
visit saw Mukherjee announce his hopes of developing a “strategic partnership” 
with Oman and the GCC, including in sectors such as education, IT, telecom, 
hydrocarbons, labour relations, science and technology, and regional security. 
The meeting between the two followed on from the signing of four MoUs during a 
four-day visit by Al Said to India in December.<br>
<br>
<b>More than oil</b><br>
A brief look at the areas covered by the December MoUs proves that more than 
just oil is at issue in the relations between Oman and India. While two MoUs 
concerned the establishment of SMEs in Oman, another related to cooperation in 
the higher education sector. Only one, regarding further development of the Oman 
India Fertiliser Company (OMIFCO), was directly related to petrochemicals.<br>
<br>
According to Krishna Kumar Gupta, CEO of Omani private equity firm Al Anwar, 
which has bought a 14.99 per cent stake in Indian financial services company, 
Almondz Global Securities, the Indian market represents an un-missable 
opportunity for Oman. According to him, India was an emerging market ripe for 
investment. “The country’s economy is consistently showing growth of 9 per cent 
per annum. It can achieve the position of the highest growing economy in the 
world and is expected to surpass the economic growth of China.”<br>
<br>
Whether or not that last part happens, it still makes good economic sense on the 
part of both parties to extend and deepen their relations. Oman is India’s 
largest single supplier of crude oil and although inflation in Oman is currently 
low and stable by GCC standards (an estimated 4 per cent in 2007), it does the 
economy no harm to let a little steam off by sending excess liquidity back from 
where it came. With oil at close to $100 a barrel, and broad money supply for 
the GCC currently growing at 15-40 per cent a year (according to Standard 
Chartered), such rationale appears even more compelling.<br>
<br>
For Oman, this represents part of a growing policy of investment in emerging 
Asian markets, which, it is no coincidence, happen to make up a large basket of 
the nation’s energy exports. For example, Oman’s US$18.69 billion exports (2006) 
were shared between Japan (22.1 per cent), South Korea (19.9 per cent), China 
(15.2 per cent), and Thailand (12.6 per cent). Whilst the first two of these, 
Japan and South Korea, can hardly be deemed emerging, there is no doubting the 
general geographic trend: Oman sells to the East and by ploughing investment 
into emerging markets there, it achieves two important things. First, it 
generates future demand for its petrochemicals, including not just crude oil, 
but also increasingly important products such as LPG and associated hydrocarbons 
involved in agriculture and industry. Second, it offsets Oman’s diminishing 
reserves by diversifying the economy into the provision of associated services, 
such as maritime and energy infrastructure.<br>
<br>
Thus, it was not surprising that Al Said raised plans to establish a joint 
holding company with India for infrastructure development, with a capital of 
US$100 million. India already manages to repatriate some of its oil budget in 
the form of remittances from its estimated 4 million GCC guest workers (400,000 
in Oman). It must be more gratifying for it though to see some direct return in 
the form of FDI.<br>
<br>
<b>Bearing fruit<br>
</b>Oman’s ‘Look Asia’ policy has already borne early fruit: work began on the 
Sohar Aromatic factory, a US$1.6 billion project with South Korean involvement, 
which will see Oman diversify into oil derivatives. The project demonstrates 
neatly what Oman stands to gain from its more developed Asian partners.<br>
<br>
Similarly, high-level visits to Pakistan and Vietnam last year laid the ground 
for Omani inward investment in these two emerging markets. In the case of 
Pakistan, the Omani Oil Marketing Company (OOMCO) is now one of the top 
contenders for the privatisation of Pakistan State Oil (PSO), which is expected 
to raise US$500 million.<br>
<br>
Earlier in 2006, the Pak Oman Microfinance Bank was established, offering 
financial services to low-income earners in Pakistan. It has recently opened its 
12th branch, and now has an operating capital of US$500 million and a client 
base of 19,000. Speaking on his return from Pakistan in April last year, Salim 
bin Said al-Ghattami, the chairman of the Oman Chamber of Commerce and Industry 
(OCCI), described Pakistan as “a moderate and investment friendly country with a 
strong economy and warm people, who make Pakistan a good mix for lasting 
business co-operation between many sectors of the two fraternal countries”. As 
with India, there is an established Pakistani guest worker community in Oman, 
sending home remittances of some US$130 million a year.<br>
<br>
Tentative steps have also been made in Vietnam, with the groundwork for Deputy 
Prime Minister Nguyen Sinh Hung’s recent two-day visit laid as far back as in 
May last year, when National Economy Minister Ahmed bin Abdulnabi Macki and 
Maqbool bin Ali Sultan, the Sultanate’s minister of commerce and industry, 
visited Hanoi. Macki has recently indicated that both Oman Oil and Oman Shipping 
may be interested in investing in Vietnam, and he has agreed to establish a 
dedicated fund for the purpose of investment and economic cooperation. The 
Vietnamese government anticipates that investment opportunities of up to US $140 
billion will emerge over the next three years, with the need for three 
refineries in particular likely to attract significant Omani interest.<br>
<br>
<b>Beyond trade?<br>
</b>It would be a mistake to characterise these developments in pure dollar 
terms though. Vietnam has identified labour exchange as a key factor in its 
negotiations with Oman, while steps toward a free trade agreement (FTA) with 
Pakistan began in 2006 and are ongoing with India (though seemingly stalled at 
the moment). The free movement of goods, capital and (to some extent) workers is 
becoming a common goal for all Oman’s emerging market partners in Asia. The end 
result will affect more than merely the economy.<br>
<br>
Rather, big emerging markets want, as Mukherjee said, “strategic partnership” 
with Oman and the wider GCC. Indian government sources have privately admitted 
that they are not content any more simply to play second fiddle to the US, not 
with close to 5 million citizens working in the GCC. The coming decade will 
feature a growing political assertiveness on the part of the bigger emerging 
markets, as economic partnerships develop into mutual geopolitical interests.<br>
<br>
In the meantime though, it is worth placing this eastern trend in perspective. 
The Institute of International Finance (IIF) took a stab recently at guessing 
where GCC sovereign wealth from the recent oil boom has been invested. Out of 
almost US$550 billion, the IIF estimated some US$60 billion had been pumped into 
Asian economies, mostly through infrastructure projects. This is a significant 
amount, yet still pales in comparison to the US$300 billion invested in the US, 
and even the US$100 billion invested in Europe.<br>
<br>
The growing attractiveness of Asia as an investment opportunity to Oman and the 
wider GCC is tempered by the drag of long-term dollar dependence: it will gather 
momentum, but slowly. In the meantime, reciprocal agreements such as FTAs and 
joint holding companies will lay the groundwork. As Oman’s oil production 
continues to diminish, and the government looks to secure the nation’s post-oil 
economy, expect to see such reciprocity increase. </font></p>

