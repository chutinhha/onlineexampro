</font><font face="Verdana" size="4">Swing your tee and remain connected</font><font face="Verdana" size="1"><br>
<img border="0" src="golf1.jpg" align="right" hspace="8" vspace="6">February 21, 2008 is an important date in the corporate calendar of Oman. The 
4th OER CEO Golf 2008 is scheduled to start at the Ghallah Wentworth Golf Club 
on that day, bringing together the head honchos of the Muscat business world. 
The event will include experienced players, amateurs and aspiring beginners 
alike. The 18-hole Stable Ford Golf tournament will be an informal but 
innovative event. More so, it will be a few hours of fun outside the boardroom 
for many a golfer. But, this fun experience for golfers as well as non-golfers 
will also be an opportunity for business interactions. OER CEO Golf has carved a 
niche for itself in the world of golf by emerging as the biggest networking 
event of Oman.<br>
<br>
OER CEO Golf 2008 will be more exhilarating with the 5-hole round for 
non-golfers and a new innovative feature called “Virtual Indoor Golf Simulator.” 
While golfers are expected to be on the turf during most part of the day, 
evenings will be rendered enjoyable playing the new golfing concept of “the 
Virtual Indoor Golf Simulator.” This time round, non-golfers can also take their 
<img border="0" src="golf2.jpg" width="200" height="293" align="left" hspace="8" vspace="6">first shot on the turf at the 5-hole exclusive golf link at leisure. It can turn 
out to be an exciting experience for those guests who will not be playing in the 
tournament, but nevertheless want to swing at the tee. In the past events of 
2005, 2006 and 2007, Golf Clinic was a very memorable experience where 
non-golfers were trained and educated by eminent professional golfers. The 
multiple rounds of ‘Crazy Golf’ of 2006, an innovative first in Oman, were 
equally enjoyable. Spouses and golf aspirants enjoyed the game at sunset and tested their skills at the mini-golf arena. Glow Golf with the fairways lit with 
candles, and glowing golf balls for the couples was no less exciting. This 
February, guests will spend the evening interacting and networking with their 
fellow participants over music, dance and sumptuous food. This will be the 
perfect time for developing current and future business relationships as it has 
been in previous OER Golf events. The evening will conclude with felicitating 
the champion and the golf aspirants with a gamut of fabulous prizes in the 
presence of the crème de la crème of Corporate Oman. Since its inception in 
2005, OER CEO Golf has been a pioneering initiative from Oman Economic Review 
aimed at promoting the game of golf in Oman. It has succeeded in adding glamour 
and character to the sport. The success of OER CEO Golf in 2005, 2006 and 2007 
witnessed upper echelons of industry converging to participate in this 
networking event and golf experience.<br>
<br>
This corporate game is clearly getting popular in Oman as the number of golfers 
is increasing every year and more golf courses are coming up. The camaraderie 
and team spirit of the high profile set of partners is expected to take the 
event to new heights. It is not just a golf event but a complete show that is 
organised by Applause Events with utmost attention to detail.</font><p align="center" style="margin: 3px 10px">
