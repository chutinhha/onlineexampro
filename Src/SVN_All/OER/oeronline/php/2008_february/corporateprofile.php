</font><b><font face="Verdana" size="4">
Power packed performance<br>
</font></b><font face="Verdana" size="1">
<b>After a splendid performance in 2007, Oman Oil Marketing SAOG is gearing up to 
grow its overseas business. OER takes stock of the company’s aggressive 
marketing strategy and thrust areas for 2008 and beyond</b></font></p>
<p align="center" style="margin-left: 10px; margin-right: 10px">
<img border="0" src="corp.jpg" width="400" height="184"></p>
<p align="justify" style="margin-left: 10px; margin-right: 10px">
<font face="Verdana" size="1">
How many CEOs will say that though their company has done well in the last year, 
there’s nothing great about it as many companies did well during the year? 
That’s exactly what Omar Ahmed Qatan, the CEO of Oman Oil Marketing Company SAOG 
said when OER asked him about his company’s performance in 2007. One needs to be 
bold and gutsy to make such a statement. It speaks of the lofty standards that 
the CEO has set for himself and the company.<br>
<br>
In its best financial performance so far, Oman Oil’s revenue increased by 26 per 
cent to RO152.7 million in 2007 from RO121.4 million in 2006. The company’s net 
profit increased by 41.5 per cent to RO5.7 million. In the Director’s report on 
the financial results for 2007, Chairman Salim Abdullah Al Rawas stated: “The 
company has expanded its market share. Overall volumes grew by 29 per cent over 
last year, the highest ever recorded in the company’s history to 1.12 billion 
litres. Strong growth was recorded in almost all products categories namely, 
gasoline, diesel, lubricants and aviation fuel.”<br>
<br>
So far Oman Oil’s focus has been on the retail business, which contributes a 
major chunk of its revenue along with the commercial business. The company has 
been actively looking at expanding its retail network overseas. Although it 
initially intended to enter Malaysia and Yemen, the project did not materialise 
due to unsatisfactory findings. “But we are keeping our eyes and ears open for 
overseas opportunities,” says Qatan confidently.<br>
<br>
<b>Lubricating returns<br>
<img border="0" src="corp1.jpg" width="200" height="115" align="right" hspace="9" vspace="6"></b>In a significant development during the year, the company launched its own 
lubricant brands – Maximo and Optimo – in the overseas markets. “We had studied 
the Yemen market quite seriously from the retail perspective. Though we have 
postponed our retail initiative there but we have applied our knowledge of the 
Yemen market to launch our lubricant brands there. We are marketing Maximo and 
Optimo in Sudan and Somalia as well. We are planning to penetrate many countries 
in the Africa, Middle East and Asia in due course,” Qatan informs. <br>
<br>
The company has entered into a partnership with Yemen’s prominent automotive 
player, Nasser General Trading, to market Oman Oil lubricants manufactured in 
Oman. Adding further, Qatan says, “Our target is to sell 1,500 MT in Yemen in 
the first year. We plan to achieve sales of 3,000-5,000 MT per annum in Yemen in 
the next five years. Overall, I foresee the lubricants business contributing 
10-12 per cent to our net profits by 2009.”<br>
<br>
In Oman, the company has an exclusive arrangement to sell BP and Castrol range 
of lubricants. Interestingly in other countries, Oman Oil’s own brands will be 
competing with BP and Castrol. But what about rolling out its own brands in 
Oman? Qatan says, “wait and watch. We will make the announcement at the right 
time”. Perhaps we need to read between the lines that the launch is on the way. 
Will it not be in conflict with the company’s arrangement with its principals? 
Qatan says, “we are committed to our partners and will maintain our high 
standards of business ethics.”<br>
<br>
<b>Customer focus<br>
<img border="0" src="corp2.jpg" width="156" height="228" align="left" hspace="8" vspace="6"></b>Moving on, by the end of 2007, Oman Oil’s service stations network expanded to 
103 outlets. With a total of 13 new Quick Shops opened in the last year, most 
recently four in December 2007 located in Barka, Al Qabil, Sohar and Buraimi, 
Oman Oil’s retail network currently includes more than 40 Quick Shops located 
across Oman. Hussain Jama Al Ishaqi, General Manager-Retail at Oman Oil informs, 
“Going forward, we are planning for continued and steady growth in 2008 which 
will include the refurbishment of old Quick Shops. More importantly, we are 
undertaking a complete re-evaluation of our Quick Shops to provide a fully 
integrated product offering to customers both inside and outside the stores. Key 
to this assessment is a customer-driven focus on convenience along with 
attractive consumer promotions.”<br>
<br>
On the commercial business front, the company recorded its best performance with 
56 per cent growth over 2006 on account of higher sales. However, Oman Oil’s 
aviation business unit suffered a setback with the ‘pullout of Gulf Air from 
Oman and stiff competition in the market’. But Qatan seems unfazed as he says, 
“we are compensated by the entry of new airlines and increase in the frequency 
of other existing airlines. With expansion of Muscat International Airport and 
opening of new airports, the business in this sector will definitely increase by 
a good margin from 2009 onwards.”<br>
<br>
On the way forward, Al Rawas says in the Director’s report, “As the competition 
is ever increasing and aggressive marketing efforts are undertaken by the 
competitors, the company will remain focused on expanding its network of 
facilities, improving the quality of its products and services, optimising costs 
and developing its human capital even further”. No time to rest on their laurels 
for 2007 as the task is quite cut out for Qatan and his team! </font></p>

