<p align="justify" dir="ltr" style="margin-left: 5; margin-right: 5"><b><br>
<font face="Verdana" size="4">Bringing tourism to Dubai</font><font face="Verdana" size="1"><br>
</font>
</b><font face="Verdana" size="1"><b>Michael Gilligan, Branding Director, Universal 
Parks &amp; Resorts, is busy ensuring that the essence of the Universal brand is 
incorporated in the development of Universal Studios at Dubailand. He shuttles 
between his base in the US and Dubai every month to make certain that the brand 
delivers its promise of becoming ‘the holiday destination of the future.’ Akshay 
Bhatnagar spoke to him on the sidelines of TDIM08 in Dubai. Excerpts:</b></font></p>
<p align="center" dir="ltr" style="margin-left: 5; margin-right: 5">
<img border="0" src="facetoface.jpg" width="400" height="310"></p>
<p align="justify" dir="ltr" style="margin-left: 5; margin-right: 5">
<font face="Verdana" size="1"><b>What’s your vision for Universal Studios in Dubailand?<br>
</b>Universal Studios, the leader in awe-inspiring entertainment for the past 
century, is creating a new tourism destination in Dubai. It will combine the 
creativity of Hollywood with the greatest stories of all time to deliver 
unforgettable experiences and an extraordinary holiday for every member of the 
family. Guests will enjoy a spectacular theme park, a variety of exciting 
entertainment and world-class hospitality starting 2010.<b><br>
<br>
How unique will be the experience for visitors at Dubailand vis-à-vis your other 
properties?<br>
</b>They will experience action-packed, exhilarating and 
one-of-a-kind-in-the-world thrills when they ‘Ride the Movies’ at Universal 
Studios Dubailand, the premier theme park in the Middle East and the star of 
Dubailand. They will enjoy music, dining, shopping, moves, and much more when 
they dance the night away on ‘The Boulevard’. And they will find enough 
electrifying memories to last a lifetime just steps away from their luxuriously 
appointed rooms in one of Universal City Dubailand’s magnificently themed 
on-site hotels or comfortable residential communities.<br>
<br>
<b>
<img border="0" src="facetoface1.jpg" width="350" height="258" align="right" hspace="8" vspace="6"></b>Our property has been designed specifically keeping in mind the unique features 
of the environment and market requirements. We have taken inspiration from our 
best experiences and applied them here. For example, the whole concept of a 
theme park based resort where you can easily walk from your hotel to the theme 
park, restaurants, movie theatres, night clubs, and so on, is quite unique. It 
makes a perfect holiday. We are going to be the first air-conditioned theme park 
in the world as the heat during the summer is unbearable here. Around 70 per 
cent of our area will be air-conditioned.<b><br>
<br>
How many properties does Universal have across the globe?<br>
</b>We started with Los Angeles, then moved on to Orlando. Thereafter, we got 
into managing a property in Spain. Next destination was Japan. We are now 
setting up one in Singapore. We are planning another one in South Korea.<b><br>
<br>
Which other markets are you eyeing?<br>
</b>As I said apart from Dubai, we are concentrating on Singapore and South 
Korea. We believe in focusing on a few projects at a time.<br>
<b><br>
When is Dubailand expected to be ready?<br>
</b>It is going to be ready in different stages. I believe some parts will be 
ready by 2009 and rest will come up in the following few years. It is not easy 
to convert 2.5 billion sq ft area of desert into a world-class tourism facility 
at one time. It is a massive undertaking.<b><br>
<br>
What is the cost of developing Universal Studios in Dubailand?<br>
</b>The total investment in Universal Studios is around US$2 billion.<b><br>
<br>
What is your share in the investment?<br>
</b>The entire investment is being made by Tatweer, a part of the conglomerate 
of Dubai Holding. It is the owner and lead developer of Dubailand.<br>
<br>
<b>Apart from the brand name, what is the role of Universal Studios in the entire 
project?</b><br>
Universal is the leading name in the development of theme parks. I have been 
personally involved in all the projects of Universal. Our role is to assist our 
partner Tatweer in the design, execution and management of the project.<b><br>
<br>
What is your revenue model in this exercise?<br>
</b>I can’t comment on this.<b><br>
<br>
How much time does it takes to recover the investment in a project of such 
nature?<br>
</b>I can’t say. It varies from project to project and market to market. For 
example, in Japan, we issued an initial public offer (IPO) in the market six 
years after the launch of the park. It depends on the ownership structure and 
objectives of the company.<b><br>
<br>
How many tourists you expect will visit Universal in Dubailand?<br>
</b>We expect around 4 million guests a year.<b><br>
<br>
Which markets would contribute to the traffic?<br>
</b>Difficult to say. Our objective has been to bring tourism to Dubai. We 
expect tourists to come from Africa, Europe, Middle East, Central Asia, other 
parts of Asia, etc.<br>
<b><br>
How did you conceptualise the park in Dubailand?<br>
</b>A research was done not only for Universal Studios in Dubailand but for many 
other entities of Dubailand. The research was done in Europe, Asia, Middle East, 
China, India, etc., to identify and understand the needs of the tourists from 
those regions. The research findings have been applied in the creation of 
Dubailand.<b><br>
<br>
Have you started promoting the property in overseas markets?<br>
</b>Not yet. We will start the communication process a year before and we will 
get into full gear six months before the launch.<br>
<b><br>
What challenges have you faced in planning and executing this project compared 
to your other projects?<br>
</b>We are in a desert. That has been the first and foremost challenge for us. 
It has been a totally different experience for us on the construction front. 
This is a wild market in terms of construction so we have made concerted efforts 
to ensure that we are able to deliver as per the scheduled time and budget.<b><br>
<br>
Do you think the cost of the development of a project of such nature is more 
expensive in Dubai?<br>
</b>I don’t think so. Our property in Japan was developed with an investment of 
US$2 billion.<br>
<br>
<b>Do you have any plan to launch a similar property in any other country in the 
Middle East?</b><br>
No. I don’t think so. Dubailand will cater to the needs of the people not just 
from the Middle East but beyond, as I said. In such a scenario, it may not be 
feasible option too. </font></p>
