        </font><b><font face="Verdana" size="4">
        Investment in Disaster Capitalism</font></b><font face="Verdana" size="1"><br>
        <b>The Shock Doctrine is the gripping story of how America’s “free market” 
        policies have come to dominate the world – through the exploitation of 
        disaster-shocked people and countries</b><br>
        <br>
        <img border="0" src="browse.jpg" width="175" height="304" align="left" hspace="8" vspace="6">As a major industrialised nation in the world, the United States never 
        fully practised laissez-faire or the virtues of a welfare state; but it 
        became the battleground for two opposing schools of economic thoughts 
        for a long time.<br>
        <br>
        Unlike Keynes who urged the need for government intervention to protect 
        the common man from economic uncertainties like recessions and 
        depressions and propagated the concept of welfare state, Milton Friedman 
        believed that to optimise efficiency and provide plenty for all, the 
        economy must be allowed to progress unhindered by government 
        interference. He suggested that drastic neo-liberal reforms, like 
        privatisation of natural resources, can be implemented by governments 
        and corporations during natural and manmade disasters when people are in 
        a state of shock and feel powerless to oppose. Under normal 
        circumstances, however, the same people would object vehemently to such 
        extreme measures. For Friedman’s theory to work, the economy has to be 
        shocked back to zero where there is no government ownership in economy.<br>
&nbsp;<br>
        It’s Friedman’s philosophies and the manner in which they are being put 
        into action that comes under Naomi Klein’s scathing attack in her latest 
        publication. The Shock Doctrine: the Rise of Disaster Capitalism 
        condemns the economics inspired by Milton Friedman as “disaster 
        capitalism.” But as is clearly enunciated by the title, it instead 
        upholds Friedman’s theory of ‘shock’. Friedman is a gifted economist, 
        reputed as an advocate of free markets, privatisation and globalisation. 
        As a professor at the University of Chicago, he preached an 
        anti-Keynesian economic programme adopted by republican presidents – 
        Ronald Reagan and George Bush. Klein argues, for the past eight years, 
        the American government implemented Friedman’s philosophy which has 
        subjected innocent civilians to untold misery and repression. <br>
        <br>
        <b>Re-engineering economies</b><br>
        The bottom-line of Klein’s thesis is that in the name of the neo-liberal 
        programme, governments around the world are taking advantage of natural 
        disasters to implement sweeping changes in economic policy at the cost 
        of the well being of the masses. It is a strategy to rob the state to 
        enrich large corporations. The shocks in democracies can either take the 
        form of coups (Chile under Pinochet), political turmoil (Eastern Europe 
        post cold war), currency crises (Mexico in the 1990s) or a military 
        invasion by the United States (Iraq). All these “shocks” have the same 
        ramifications: sale of state assets, opening of markets to foreign 
        investments, elimination of price controls for stable supplies.<br>
        <br>
        Klein deduces that the emerging ‘new economy’ generates profits from 
        other peoples’ misery. In Iraq, in the name of reconstructing the 
        country, the American government has been systematically selling the 
        country’s industry and resources and services to big American 
        corporations or private hands. In a big way, a new investment 
        opportunity has grown. Any opposition to these changes are squelched 
        through political machinery as the Bush Administration has done in Iraq 
        through Homeland Security.<br>
        <br>
        In a lucid, informative thesis, Naomi Klein’s presentation is not 
        pretentious and the footnotes are done meticulously. For Klein, it was a 
        lot of hard work. She went round the globe, from post-tsunami Indonesia, 
        Sri Lanka, New Orleans to Iraq and many other affected places, 
        conducting interviews, and probing situations where the shock doctrine 
        has been implemented. To many, nothing in this book may seem new; but it 
        is an eye-opener for many others. And even if you don’t see eye to eye 
        with Klein’s left-leaning ideas, this book is worth reading because it 
        is a book about politics of economy.<br>
        <br>
        – <b>Susmita De </b></font></p></td>
