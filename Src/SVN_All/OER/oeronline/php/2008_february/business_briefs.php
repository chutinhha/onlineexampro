</font><b><font size="2" color="#CC3300" face="Verdana">Muscat Festival gets bigger and better<br>
</font></b><font face="Verdana" size="1">
<img border="0" src="bus4.jpg" width="250" height="146" align="left" hspace="8" vspace="6">With Qurm Natural Park, the main venue of the Muscat Festival, managing to 
recreate the culture and heritage of Oman with great precision, Muscat Festival 
2008 got off to a splendid start in January. Enthusiastic visitors are enjoying 
various events and stalls at a number of festival venues. The corporate houses 
have left no stone unturned to start the year on a good note by offering 
attractive promotions of their products. BankMuscat is offering free credit 
cards and ROP prepaid card free of cost besides free gifts and cash prizes. 
BankDhofar is promoting its saving scheme Al Heson with special draws. Oman 
Mobile is offering Hayyak SIM cards for RO2, which includes a free talktime of 
RO1. Not to be left behind, Nawas is offering prepaid connection for RO2 with 
free talk time worth RO2. Oman Air is promoting its special packages with 
attractive fares and destinations.<br>
