</font><font size="2" color="#CC3300" face="Verdana">Jet Airways launches flights from Muscat</font></b><font face="Verdana" size="1"><br>
<img border="0" src="bus6.jpg" width="175" height="106" align="left" hspace="8" vspace="6">Jet Airways has forayed into the Sultanate of Oman. The airline formally started 
its daily direct flights to Kozhikode (Calicut) and Kochi in India in January. 
The airline is flying all these destinations non-stop everyday. Offering 
world-class service on the advanced Boeing 737-800 aircraft, Jet Airways is 
acclaimed for high quality of comfort, reliability and high standards of 
in-flight and ground services. Jet Airways’ flights to 57 destinations span the 
length and breadth of India and beyond, including New York (both JFK and 
Newark), Toronto, Brussels, London (Heathrow), Singapore, Kuala Lumpur, Colombo, 
Bangkok, Kathmandu, Dhaka, Kuwait and Bahrain. The airline plans to extend its 
international operations to other cities in North America, Europe, Africa and 
Asia in phases, with the introduction of additional wide-body aircraft into its 
fleet.<br>
