Oman Arab Bank has launched an India-centric wealth management product and 
services scheme that will provide an opportunity to potential investors in the 
country to invest in the booming Indian economy. Under the scheme, India Access, 
Oman Arab Bank-Investment Management Group has tied up with Kotak Securities Ltd 
