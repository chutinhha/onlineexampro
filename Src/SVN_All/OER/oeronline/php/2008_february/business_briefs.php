<font size="2" color="#CC3300" face="Verdana"><b>Bank Sohar revolutionises credit cards’ market</b></font><font face="Verdana" size="1"><br>
<img border="0" src="bus1.jpg" align="right" hspace="9" vspace="6">Bank Sohar’s hunger for capturing a significant market share in the credit card 
market is evident from the aggressive marketing strategy it has adopted ever 
since it entered the segment in April 2007. Its latest salvo is the launch of 
the Excel range of credit cards. The bank is making available three variants of 
its Excel credit cards, starting with the Classic credit card, which comes to 
customers free for life. The supplementary Classic cardholder also enjoys the 
same privilege. This benefit is for a restricted period and is being offered by 
way of an introductory offer only. <br>
<br>
Bank Sohar’s Excel Platinum and Gold Credit cards are intended for the upper 
echelons of customers and come with free membership for the first two years. In 
addition, Platinum and Gold credit cardholders will receive a complementary 
priority pass membership card, providing them access to over 500 airport VIP 
lounges in 90 countries and over 275 cities, irrespective of their choice of 
carrier, class of travel or place of travel. The membership also entitles the 
cardholder to a host of facilities at the VIP lounges. Platinum credit 
cardholders have more to smile about as they are being offered an automatic 
advance travel insurance cover for US$2 million of purchase of air tickets 
through their credit cards.<br>
<br>
</font><font size="2" color="#CC3300" face="Verdana"><b>United Dates’ targeting US and Australia markets</b></font><font face="Verdana" size="1"><br>
United Dates Factory Co, which has 87.5 per cent Omanisation, is the only 
company in the date industry to have been given the ISO 9001 accreditation for 
quality management and ISO 22000 for HACCP – Food Safety Management System. 
According to a company spokesperson, “We have a very ambitious mission to take 
Oman to the top of the world date industry map. Our list of countries where we 
export include India, Malaysia, Japan, Canada, Holland, South and East Africa(s), 
Indonesia, Syria, Yemen, and Sri Lanka apart from Qatar and UAE. Negotiations 
are underway for exports to Cyprus, Australia and the US. Besides, we have a 
good foothold in the local market”. The company has a processing capacity of 
over 3,750 tonnes per annum.<br>
<br>
</font><b><font size="2" color="#CC3300" face="Verdana">Octal Petrochemicals gets awarded by Maersk Logistics<br>
</font></b><font face="Verdana" size="1">
<img border="0" src="bus2.jpg" width="200" height="135" align="left" hspace="8" vspace="6">Octal Petrochemicals, a subsidiary of Octal Holding and Co. SAOC, has received 
an award from Maersk Logistics in recognition of its rapid export growth through 
the Port of Salalah. The Oman-based plastic packaging leader began shipments in 
February last year and moved 1,500 TEU (twenty-foot containers) through the port 
in the start-up phase for export to international markets such as Europe, North 
America, the Far East and India. Klaus Dahl Tindborg, Regional Director of 
Maersk Logistics Middle East, said: “We have made this award to demonstrate 
Octal’s importance as a Maersk customer, and to show that we fully support the 
company’s ambitious expansion plans”. Maersk Logistics aims to increase its 
service portfolio in Salalah and to support the expansion of the Salalah Free 
Zone, as it becomes a world-class logistics hub.<br>
<br>
Octal’s global sales exceeded 2,000 tonnes last November and the company plans 
to capture as much as a fifth of the US$2 billion world market for APET sheets 
by the middle of this year. A delegation of the AP Moller – Maersk Group, 
including the company chairman, board of directors and chief executive, recently 
visited the Octal Petrochemicals facility in Salalah. The delegation also viewed 
construction work at Octal’s integrated PET resin and APET sheet packaging 
plant, which will come on stream by April.<br>
<br>
</font><b><font size="2" color="#CC3300" face="Verdana">Manufacturing on an upswing</font></b><font face="Verdana" size="1"><br>
Oman’s manufacturing sector is on the rise and for many companies export order 
books are full, reveal recent figures. “The figures are highly encouraging. 
Clearly, we’re witnessing a fundamental shift in our economy and it is being 
driven by globalisation, knowledge, technology and innovation, and it is quickly 
changing the nature of manufacturing and the type of workforce and skills we 
need in the sector,” says Hilal Al Ahsani, CEO, Public Establishment for 
Industrial Estates (PEIE). “Today’s manufacturing sector is knowledge, skills, 
innovation and is enterprise driven, and if we’re to keep export order books 
full, we need to ensure that manufacturers stay focused on these key issues.”
<br>
<br>
Al Ahsani suggests that one way of fostering innovation and enterprise in 
manufacturing is to strengthen the sector’s ties with higher education. In early 
2007, PEIE introduced the Oman Manufacturing Group (OMG), a quarterly networking 
seminar programme that brought together a core of enthusiastic and energetic 
individuals from manufacturing and academia, committed to developing and 
sustaining a strong future for Omani manufacturing. OER is a media partner of 
OMG seminar.<br>
<br>
</font><b><font size="2" color="#CC3300" face="Verdana">Oman Air in pact with Etihad</font></b><font face="Verdana" size="1"><br>
<img border="0" src="bus3.jpg" width="200" height="108" align="right" hspace="8" vspace="6">Oman Air and Etihad have signed an agreement, which will allow passengers of 
both carriers to take advantage of their frequent flyer programmes. Eithad 
Airway’s Regional General Manager, UAE &amp; Oman, Raymond Korban stated that 
“Etihad Airways is delighted to be signing this agreement with Oman Air to 
enable members of our respective loyalty programmes, Etihad Guest and Sindbad, 
to earn miles when flying with both airlines. Both Etihad Airways and Oman Air 
are the national carriers for the UAE and Oman, and therefore to cooperate 
together like this is a great achievement for both of these proud nations.” In 
this regard, Abdulrazaq Al Raisi, Oman Air’s Senior Manager Sales, stated that 
the objective of the partnership is to offer more benefits, mileage earning and 
redemption opportunities to Sindbad Frequent Flyer Programme and Etihad Guest 
FFP members by enhancing the FFP product, to get competitive advantage through 
Etihad network. This agreement will allow Oman Air’s Sindbad members to accrue 
miles and redeem miles when travelling on Etihad flights.<br>
