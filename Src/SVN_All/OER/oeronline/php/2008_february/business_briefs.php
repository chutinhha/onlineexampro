</font><font size="2" color="#CC3300" face="Verdana"><b>Daewoo gets Duqm 
contract<br>
</b></font><font face="Verdana" size="1">Daewoo Engineering &amp; Construction Co., 
South Korea’s largest builder, has received a contract to build a repair 
shipyard for US$309 million from the Ministry of National Economy. The project 
in the south-eastern city of Duqm is worth US$443 million. The repair yard will 
have two 410-metre drydocks and will take 30 months to build, Daewoo Engineering 
said. The South Korean company bid together with Galfar Engineering &amp; 
Contracting SAOG.<br>
<br>
</font><font size="2" color="#CC3300" face="Verdana"><b>Oman in Boston Seafood 
Show </b></font><font face="Verdana" size="1"><br>
Following the successful participation at the International Boston Seafood Show 
2007, the Omani Centre for Investment Promotion and Export Development (OCIPED) 
in coordination with the Directorate General of Fisheries, Ministry of Fisheries 
will be participating in the International Boston Seafood Show to be held during 
February 24-26 at Boston, USA. Four Omani fish exporting companies will be 
participating under “Oman Pavilion”. <br>
<br>
GBCM launched in Oman Europe’s top securities trading banks and the largest 
brokerage firm in Oman have formed a Muscat-based joint venture investment 
company offering a host of services such as brokerage, corporate finance 
advisory services, asset management and access to global markets for its clients 
in the GCC region. The new RO6 million Gulf Baader Capital Markets (GBCM) is 70 
per cent owned by Oman’s Gulf Investment Services (GIS), 25 per cent by Baader 
Wertpapierhandelsbank of Germany and 5 per cent by Silver Circle Overseas. 
Following the tie-up, the 14-year-old GIS, the Sultanate’s top brokerage company 
in terms of trading volumes on the Muscat Securities Market (MSM), has ceased to 
exist.<br>
