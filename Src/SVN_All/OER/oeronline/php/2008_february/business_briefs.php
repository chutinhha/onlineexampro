</font><b><font size="2" color="#CC3300" face="Verdana">GroFin – IEF finances RO1.4 mn to SMEs</font></b><font face="Verdana" size="1"><br>
<img border="0" src="bus5.jpg" width="200" height="126" align="right" hspace="8" vspace="6">Before the end of its first year of operations, GroFin approved finance of six 
projects. Abeer Al Abduwani, General Manager of GroFin Oman says, “GroFin is the 
fund manager of the Intilaaqah Enterprise Fund (IEF), a US$10 million fund, 
contributed by Shell to the Sultanate of Oman. It consists of a revolving fund 
dedicated to small and medium-sized enterprises (SMEs).”<br>
<br>
“GroFin provides business development assistance and financing to start ups and 
growth capital to existing businesses to assist in the development of SMEs in 
Oman. Most societies today consider SMEs to be the backbone of the economy and 
the development of SMEs is essential to sustain economic growth and employment,” 
commented Abeer. GroFin specialises in business development and the provision of 
finance to SMEs, and currently works in six countries in Africa. It commenced 
operation in the Sultanate of Oman earlier this year. The primary focus of 
GroFin is SMEs with less than 50 employees, an annual turnover of less than RO2 
million and gross assets of less than RO1.2 million.<br>
