<p align="justify" dir="ltr" style="margin-left: 5; margin-right: 5"><b><br>
<font face="Verdana" size="4">Emerging markets:
Resilient, for now</font><font face="Verdana" size="1"><br>
The emerging markets appear to have learnt the lessons of the 1990s all too well 
and any idea of a recession in these markets appears unthinkable in 2008, though 
some slowdowns are possible<br>
<br>
</font></b><font face="Verdana" size="1">
<img border="0" src="periscope.jpg" width="126" height="193" align="right" hspace="8" vspace="6">Emerging markets are often the immediate victims of a global financial crisis. 
In fact, the devaluation of the Mexican peso in 1994, the Russian default in 
1998 and the Asian currency, banking and equity meltdown in 1997-98 were seminal 
events in international finance; crises whose trigger and epicentre lay in the 
emerging markets. Yet, the old equation between risk aversion and the emerging 
markets seems to have broken down in the case of the current Wall Street credit 
crunch, the world’s most traumatic financial crisis since the collapse of the 
Silicon Valley and Internet stock bubble in 2000. Emerging markets have proved 
remarkably resilient since last summer, with markets in Brazil, Russia, India 
and the GCC even rising to new highs.<br>
<br>
There are several reasons for the resilience of emerging markets as an asset 
class. One, the spectacular bull market in commodities, particularly gold, oil 
and the industrial metals, has not been derailed by the credit crunch. Emerging 
markets such as the GCC, Russia, Brazil and South Africa, are highly correlated 
with the bull run in oil, gas and commodities markets. Two, the banking distress 
on Wall Street has compelled the world’s central banks to intervene in the money 
markets, bringing down three-month Libor rates from 5.25 per cent to 4.50 per 
cent in the past six months. In essence, lower interest rates are a steroid shot 
for valuation multiples in the emerging markets. This is particularly true in 
the markets where a currency peg exists against the dollar. So when the US 
Federal Open Market Committee (FOMC) cut dollar rates in September, it was no 
coincidence that the major beneficiaries were indices such as Saudi Arabia’s 
Tadawal and Hong Kong’s Hang Seng, where currency pegs meant a surge in money 
supply and a liquidity wave. Three, China, India, Russia and GCC are all 
exhibiting 8-10 per cent GDP growth. The idea of a recession in these markets is 
unthinkable in 2008, though slowdowns are possible.<br>
<br>
<img border="0" src="periscope1.jpg" width="250" height="166" align="left" hspace="8" vspace="6">Of course, the real test of emerging markets’ resilience will come when the US 
economy slips into full-scale recession. However, the fact remains that the 
emerging markets have learnt the lessons of the 1990s all too well. Banking 
systems are no longer weak, corporate balance sheets are not as leveraged, the 
sovereign credit cycle has never been more compelling, with Russia upgraded from 
default to investment grade in a mere decade and South Korea no longer the 
debtor’s ward of the IMF. Domestic demands have created new realities in markets 
like India, where exports are still barely 15 per cent of the GDP.<br>
<br>
<b>Opportunities galore</b><br>
Lower interest rates, deregulation in securities exchanges, US$95 per barrel 
crude and the rise of the institutional investor (local, regional and global) 
has meant extraordinary money-making opportunities for the GCC equities markets 
in 2007. For instance, Oman’s Galfar Engineering IPO, up 150 per cent from its 
offer price in only three months, attracted fund managers of the calibre of Dr. 
Mark Mobius, Templeton’s star emerging markets manager. Indian IPOs have 
attracted international investors and Chinese deals, from Alibaba.com to China 
Railways, continue the primary market success that was set off when ICBC, Bank 
of China and the China Construction Bank floated in Hong Kong and Shanghai to 
create the world’s largest market cap banking empires.<br>
<br>
Since the epicentre of the credit crunch is the US and Britain, investors have 
continued to allocate more funds to the emerging markets, particularly the BRIC 
majors. This increased fund flow reflects the basic fact that, with US$3 
trillion in central bank reserves, emerging markets are now a net creditor to 
the US and often run significant current account surpluses against Washington. 
This is the reason, unlike 1994 or 1998, why emerging markets sovereign credit 
risk spreads have not widened. In fact, the JP Morgan emerging debt index is 
only 235 points above Uncle Sam’s IOUs, extremely low in the context of the past 
decade, when it was as high as 1,200 basis points above US Treasury debt in 
2002.<br>
<br>
It is no coincidence that sovereign wealth funds from the UAE, Kuwait, China and 
Singapore have acted as white knights for Wall Street money centre banks and 
investment banks such as Citigroup, Merrill Lynch and even the UBS. Moreover, 
emerging markets are no longer as dependent on exports to the US, though 
decoupling is impossible if recession deepens in the US, Eurozone, Britain and 
Japan. Emerging markets also face significant inflation risks. The People’s Bank 
of China, alarmed by social unrest created by the rise in food prices, has 
tightened banking reserves ratios to combat the Shanghai A share bubble. 
Inflation rates in the GCC have also surged due to higher rents, cement prices 
and the existence of the currency peg, which forces central banks to cut 
interest rates in tandem with the Fed.<br>
<br>
<b>Correction still possible</b><br>
It is critical not to underestimate the risks of an emerging markets correction 
in 2008. One, GDP growth rates in Asia could fall as the Western economies drift 
into recession and Chinese austerity measures seek to restrain stock and 
property bubbles ahead of the Beijing Summer Olympics. Two, if commodities fall 
due to a global recession, Latin America’s major stock market could easily fall 
20-30 per cent in a matter of weeks. Three, valuations in some emerging markets 
are now excessive relative to their own past range. For instance, India and 
China trade at 25 and 35 times earnings, making them two of the most expensive 
stock markets. Oman’s MSM trades at 9500 or almost 15 times earnings. Bank 
Muscat, whose GDR is widely owned by international investors, trades at a 
valuation premium in excess of 20 times earnings. While Oman’s valuation 
multiple has been re-rated due to the MSM’s superior earnings growth, regulation 
and cross-border capital inflows, it is doubtful if the market multiple will 
expand in 2008 while the corporate earnings momentum decelerates.<br>
<br>
The Hang Seng index has corrected from its October high of 31,600 to 25,800 now, 
a 16 per cent correction in two months. Hong Kong has been dragged lower by 
HSBC, whose shares are 20 per cent of the index and are hurt by its exposure to 
US subprime, UK mortgages and bearish capital markets. Yet, the Hang Seng below 
25,000 could well be attractive for long-term investors, given negative real 
interest rates when the Fed cuts again and its exposure to China’s incredible 
growth story. </font></p>
