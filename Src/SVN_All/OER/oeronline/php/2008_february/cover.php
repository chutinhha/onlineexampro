<p align="justify" style="margin-left: 10px; margin-right: 10px">
<font face="Verdana" size="1">
<b>With demand for hi-tech gizmos, zippy cars, MNC fast food brands, stylish 
fashion accessories and craze for international pop music zooming up in Oman, 
the marketing for youth has reached the tipping point. OER looks at the changing 
dynamics of youth marketing in Oman</b><br>
<br>
Oman is predominantly a young country, or to put it better, a country of the 
young. With over 50 per cent of the population aged 16 and below and a rapid 
rate of growth of the population, the number is expected to double within the 
next two decades. To an entrepreneur, or any company marketing its products, the 
young clientele (16-24 years) segment remains the most exciting as well as 
perhaps the most perplexing, not only in the Gulf region but also the world 
over. And here in Oman, young men and women have never had it so good.<br>
<br>
<img border="0" src="cover2.jpg" width="250" height="303" align="right" hspace="8" vspace="6">With a high percentage of young population (16-24 yrs) with exposure to global 
youth trends, young Omanis are not drastically different from their counterparts 
in other parts of the region or the rest of the world. Often armed with foreign 
degrees and well-paying jobs, Omani youth are becoming increasingly demanding, 
brand-conscious and very particular about getting good value for their money. 
The ‘marketing for youth’ concept has apparently reached the tipping point. 
Marketers here, on their part, are also going all out to promote their products 
and brands among the youth. Several companies target this group. So how do they 
tap into that fertile market? We, at OER, speak to some of them about the 
multiple strategies and tactics they adopt and how companies in Oman are gearing 
up to tap the potential this market segment offers.<br>
<br>
<b>The new face of youth</b><br>
Marketers feel that Omani youth have changed drastically in the past few years. 
They want to own swanky cars and the latest gadgets. They patronise MNC fast 
food brands, high-end clothing brands and fashion accessories. Jayanta G. 
Borpujari, Divisional Manager, Computer and Communications Systems, Khimji 
Ramdas, that markets Motorola mobile handsets in Oman, says: “Today’s youth here 
are significantly different than what they were before. They are more outgoing 
and fashionable. You see them spend time with friends in net cafes, shopping 
malls, coffee shops, etc. with remarkable changes in their choice of dress and 
personal effects.<br>
<br>
“They look for value in any product they pick, be in terms of aesthetics, 
trendiness or economy. A national or an expatriate, they are all similar in 
their aspirations though different in their outlooks. Essentially, we see a 
vibrant, trendy and resourceful youth segment emerging in the market here just 
the way it has around the world. This is critical segment no marketer can afford 
to ignore.”<br>
<br>
<img border="0" src="cover3.jpg" align="left" hspace="8" vspace="6">However, Sridhar M, Deputy General Manager, Procter and Gamble Division, Khimji 
Ramdas has a different take. “The cultural differences between an expatriate 
youth and a young Omani are interesting. I think the aspirations tend to be 
different. But I hope that’s good for the economy…a lot of young Omanis are 
getting into the private sector, the education levels are pretty high”. It is 
hardly surprising then that the average Omani youth starts thinking and planning 
about having his/her own car at age 16. And the young population of Oman seem to 
fancy the rather sporty and trendy models from Japan, Europe and US 
particularly. The carmakers have been quite innovative in their marketing and 
branding strategies and clearly target the young clientele, whether it is by 
strategically placing big banner ads/cutouts or participating in youth-oriented 
activities such as bowling competitions or arranging movie screenings.<br>
<br>
One of the challenges for marketers is to keep track of the fast-changing 
trends. Most companies seem to be on the right path in this regard; they 
advertise in spots which the youth tend to frequent, and also organise events in 
schools and universities. Rasha Jamali, Manager, Internal and External 
Communications, Oman Mobile, says, “Our Gamer programme, under which a product 
development team goes to the universities here and get the students to develop a 
concept for a mobile game, has been well received and is becoming more and more 
popular. It’s very exciting and intensive for the students. The students who 
come up with the concept that is chosen are sent abroad to train with actual 
programmers and jointly work out the game.” <br>
<br>
Oman Mobile is also heavily involved with sporting events, such as football and 
car racing, bowling and volleyball. The company has recently tied up with the 
educational portal launched by the Ministry of Education. Other corporate 
tie-ups are with Pepsi, Kitkat and McDonalds, which are all brands that young 
people can relate to. In order to gauge the fast changing consumer behaviour, 
many companies go the extra mile and do market research. “We work with market 
research agencies to understand trends at least once a year. We understand that 
there is a huge human element in branding…a good sound knowledge of running the 
business has long been and continues to be our competitive edge. Very often, we 
have to reinvent products to keep the brand abreast with the expectations of the 
young. Co-marketing is a big thing today. It’s a necessity that we execute those 
plans,” says Sridhar M. <br>
<br>
<b>The business of being young</b><br>
When looking at the concept of youth marketing, it is important to consider how 
much of companies’ business/sales come from the youth segment. For Motorola, 
business from the youth segment is one of the cornerstones in company’s 
marketing strategy.<br>
<br>
<img border="0" src="cover4.jpg" width="300" height="245" align="right" hspace="8" vspace="6">According to Borpujari, Motorola’s business falls into two broad categories. 
“One is for the upwardly mobile executives who look for sophistication, utility 
and elegance in their handsets. The other is focused on the young and 
not-so-olds with young hearts. In this range, the emphasis is on music with 
optics as the next. Many models in Motorola’s range of handsets today are 
designed to address the youth segment.” He adds,”2008 will be watershed year for 
us. Our share of the market has grown steadily in last few years. But, we are 
going to take a leap this year. With an enlarged marketing team and a major 
drive to increase spread of active retailers, our market share will see a 
significant boost this year. In this effort, we shall do all we can to convince 
customers in the youth segment to make Motorola their first choice. We are 
considering few campaigns meant exclusively for the youth this year. We have a 
strong story to tell with some of the new products on their way.”<br>
<br>
He points out that over 60 per cent of Motorola’s business in Oman comes from 
the youth segment in terms of quantity. He adds that only 15 per cent of the 
business comes from Oman’s interior areas, i.e. excluding Muscat and Salalah. 
However, he contends that business from the interior regions is bound to see 
solid growth. “Sohar is also booming – We are doing good business from that 
area.” Gillete prefers to look at their businesses category by category. OER has 
learnt that beauty care business in Oman, in the categories P&amp;G operates, is 
worth close to RO25 million. “Like in female grooming, Oil of Olay is a big 
business for us in Oman…it has more than doubled in the last two years. The 
economic strength in the country is translating into good purchasing decisions 
made by the Omani consumers. A lot of the business comes from the youth segment. 
Around 60 per cent of this (beauty care business) is centred around youth,” says 
Sridhar M.<br>
<br>
<img border="0" src="cover5.jpg" width="200" height="288" align="left" hspace="8" vspace="6">Rasha Jamali says it’s very hard to tell how much of Oman Mobile’s clientele is 
youth. “But given the fact that there is such a high number of youth in Oman, 
and with the response that we have had to our services… they are primarily used 
by the youth”. Firms like Gillette are now investing more and more money into 
getting their message across to the youth. Says Sridhar, “Our youth-oriented 
budget is growing year-on-year. The company thinks it’s essential that we 
connect with the young people early and retain them.<br>
<br>
<b>Reaching out… and how? <br>
</b>Saying it is fine, but saying it well is everything. Some marketers in Oman 
actually had to completely change some of the ad designs they came up with 
recently as they realised that luring the youth needed more colourful, jazzy 
campaigns. OER found an interesting assortment of responses on how best to 
attract the attention of the young and interestingly, the traditional print 
media, which includes newspapers and magazines, has been given the thumbs down 
from most quarters. The focus is on having the right brand ambassadors and 
having a presence in the right places. <br>
<br>
Rasha Jamali says, “It has never been easy to reach out to youth here…in fact, 
worldwide it is known to be quite hard. We at Oman Mobile have used the 
traditional print media, including magazines, to make our presence felt. But 
there is a definite need to go beyond that…like we go into shopping malls, 
cinema halls and universities. The electronic medium and maybe mobiles are what 
will get through to them. We also plan to be heavily involved in websites, the 
Internet. There is a definite need to look at alternatives to reach out to the 
youth.”<br>
<br>
Though most marketers still remain apprehensive about Internet with penetration 
being quite low in Oman, Rupesh Bhatnagar, General Manager, National Publishing 
and Advertising LLC, is quite positive. “The media scenario has totally changed. 
Today’s Omani youth are very much a part of the online or virtual community. 
That’s where they tend to mingle. It must be mentioned that the youth are 
increasingly getting familiar with the Web”. When asked about advertising on the 
Internet, Borpujari replies, “We haven’t really tried going online with our 
campaigns. We now focus a lot on point of sale promotions. We may be looking at 
sponsoring entertainment programs this year. Idea is primarily to drive home 
Motorola’s identification with music and entertainment, something which holds 
the key to hearts of youth anywhere and everywhere in the world. Oman is 
certainly no exception to this.”<br>
<br>
Sridhar says, “If you look at the brand icons for our latest product Gillette 
Fusion, you have Roger Federer, Tiger Woods, Rahul Dravid and Thierry Henry. 
These are all youth icons. Anybody who is aged between 16 and 24 would aspire to 
be in a situation like that and identify themselves with these people. In that 
regard, I think we’ve been very successful in bringing those big names in and 
making connections with the local shoppers.”<br>
<br>
He feels that the challenge in getting through to the youth is exciting. “It’s 
fun and it will be that way as long as you enjoy the challenge. Only those who 
think ahead will probably continue to succeed. As long as you continue to create 
value for your shoppers irrespective if what age group they are in, they will 
continue to reward you. If you look at the current crop of shaving systems, I 
don’t think there is anybody close to even matching the kind of efficacy or the 
kind of product line that Gillette has.”<br>
<br>
He elaborates, pointing out that the company does not advertise on Oman TV as 
the rate disparity for locally manufactured brands and MNC brands are too steep. 
“We do a lot of in-store consultancy in the hypermarkets. Our products, like the 
newly launched ‘Fusion’ razor, are available wherever shoppers go, every buying 
point…from a hypermarket to a grocery store…we call that the first moment of 
truth...when the shopper interacts with our brands and makes a decision to buy. 
The second moment of truth is when the shopper uses our brands, experiences 
them. These are the two areas where we strive very hard to do well in.”<br>
<br>
Rasha Jamali strongly believes that communication to the youth has to be 
different from the overall strategy. “The message has to be different…it has to 
have a younger look and feel to it. You shouldn’t lose your brand identity but 
it has to be more fun. For instance, it has to be quite different from what we 
do with the stock market alerts! That way, the response to our Gamer programme 
has been fantastic.”<br>
<br>
Sports, especially football, seem to be the one strategy that marketers, from 
Oman Mobile to Mountain Dew, seem to swear by. Mountain Dew, one of Pepsico’s 
most popular brands, is emphasizing its appeal to the youth of today who want to 
add new and exciting experiences to their lifestyle. In the minds of today’s 
teenagers, Mountain Dew and extreme sports gells nicely together and by 
presenting the Dubai X Games, Mountain Dew is encouraging the region’s youth to 
develop a go-for-it and get-it-all attitude. Pepsi has come up with the Pepsi 
Street Football Challenge, an innovative and unique programme designed to foster 
a new generation of football stars in the GCC.<br>
<br>
<b>‘Test’ing times</b><br>
There is no denying that marketers face varied challenges when dealing with the 
young brigade. “The biggest challenge has been and will remain getting the 
loyalty of the youth and retaining their patronage. It’s about figuring what we 
need to have for the youth market, for the expatriate market…we do look at 
different market segments and try to provide what they need,” says Rasha. The 
youth are very changeable in their habits, whether it is getting new handsets 
every few months or switching between brands of, say, perfumes. The 
near-constant motto seems to be ‘try something new.’ This particular segment 
(16-24) is a very risky one as there is also peer pressure – they get influenced 
by their contemporaries, even rival brands. “It’s about staying ahead of the 
race and creating value for your shoppers. Catch them early and they will stay 
with your brand for a lifetime,” says Sridhar. He seems to have hit the right 
note. Today’s teenager is potentially tomorrow’s regular customer.<br>
<br>
<img border="0" src="cover6.jpg" width="300" height="251" align="right" hspace="8" vspace="6">Another challenge, as Bhatnagar points out, is that FMCG is what leads players 
to youth marketing. “Oman as a market has very few FMCG players…for them, 
marketing and advertising is equal to and all about ‘being on a shelf.’ The 
beverage industry is one which is hot on the heels of the youth. In terms of 
consistency, in terms of pursuing the youth, Mountain Dew has been a leader in 
Oman. The other is Red Bull. It’s more about sports and event marketing. In my 
opinion, other segments don’t really market to the youth, except maybe the 
automotive sector.”<br>
<br>
Yet another issue that crops up is whether the people who call the shots in 
marketing divisions of various companies, mostly expatriates, really understand 
the Omani youth and what they actually want. Bhatnagar says “Some of the 
ad/marketing campaigns may not be appropriate…yes, it can be said that sometimes 
the decision makers do not really figure out what the young people want.”<br>
<br>
Sridhar offers a counter view. “We understand that there is a huge human element 
in branding…a good sound knowledge of running the business has long been and 
continues to be our competitive edge. We operate as a team. It’s not just one 
person but a whole team which makes decisions. We’ve been here for 25 years. 
People who hope to make a few quick bucks won’t last. We’re not here to come in 
and go away.”<br>
<br>
<b>Looking ahead<br>
</b>With more and more companies in Oman preparing to take on the puzzle that is 
youth marketing, we can expect some strong changes in the coming years. More 
youth-oriented products, aggressive promotional campaigns, a potential Internet 
marketing boom…all these are distinct possibilities. Yes, the marketers are at 
work. They all want a piece of the action in ‘youthful’ Oman. As Sridhar puts 
it, “Exciting times are ahead.” 
