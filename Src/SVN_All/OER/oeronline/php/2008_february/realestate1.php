<p align="justify" dir="ltr" style="margin-left: 5; margin-right: 5">
<font face="Verdana" size="1">After ‘The World’ and many other iconic real estate development projects to its 
credit, Nakheel, one of the world’s largest privately held property developers, 
is undertaking a new landmark project. ‘The Universe’ is a reclaimed island 
development which draws its inspiration from the wonders of the solar system, 
with islands in the shape of the sun, the moon and the planets. Nakheel’s total 
developments spread across more than 2 billion sq ft of land and its projects 
are projected to be worth US$60 billion.<br>
<br>
The Universe will be located off Dubai’s coastline close to The Palm Jumeirah, 
inshore from The World. The development is expected to create new luxury living 
space, commercial opportunities and additional coastline, playing a role in the 
continued economic growth and success of Dubai as a global business and tourism 
destination. It is expected to be ready in 15-20 years.<br>
<br>
According to a company spokesperson, “The Universe is at an initial concept 
stage and will evolve following engineering, feasibility and environmental 
studies that are already underway. Once these studies have been conducted, a 
full master plan will be created. The Universe will be a flagship development of 
Nakheel’s recently launched sustainability initiative, Blue Communities, 
reflecting a firm commitment to creating a truly sustainable community. Blue 
Communities seeks to fund research and development activity, and promote active 
engagement with international experts on the issues of sustainable development, 
construction, management and governance of coastal communities around the 
world.”<br>
<br>
Nakheel is committing AED500million (RO50 million) over five years, plus its 
expertise and experience, to raise awareness of issues specific to coastal 
development, engaging thought leaders, NGOs and other stakeholders to take an 
active role in developing the Blue Communities initiative. Sultan Ahmed bin 
Sulayem, Executive Chairman, Nakheel said: “Blue Communities is a call to 
action. Almost two-thirds of the world’s population live in coastal communities 
and a large amount of development is taking place in these locations. As such 
there is a need for significant investment in research, development and positive 
transformation of these environments. The long-term aim is for Blue Communities 
to provide leadership in the development of sustainable coastal communities.”<br>
<br>
In another initiative, Nakheel is doing the redevelopment of Port Rashid. Given 
the change in the market, port dynamics and the rise of Jebel Ali Port, port 
operations are moving from Port Rashid to Jebel Ali Port. “Nakheel is committed 
to preserving and expanding this site of historic importance with a new urban 
waterfront that will create real estate, generate jobs, attract tourism and help 
drive economic growth for Dubai and the UAE. Redevelopment plans are underway, 
which involve the expansion of the existing Port Rashid land area through land 
reclamation. The proposed vibrant and mixed use urban waterfront development is 
set in Port Rashid’s strategic waterside location and proximity adjacent to Bur 
Dubai. The estimated residential population will be 200,000,” the Nakheel 
spokesperson informed. </font></p>
