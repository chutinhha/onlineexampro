</font><b><font face="Verdana" size="4">
Online education redefined<br>
</font></b><font face="Verdana" size="1">
<b>Sitting in front of your computer right here in Oman, you can now have access 
to some of the most popular undergraduate courses offered by Yale 
University&#8194;|&#8194;By Rekha Baala</b><br>
<br>
<img border="0" src="education.jpg" width="199" height="297" align="left" hspace="8" vspace="6">Freely available to anyone in the world with access to the Internet, Open Yale 
Courses (http://open.yale.edu/courses/), as the programme is dubbed, is 
redefining the concept of online courses making it possible for a global citizen 
to experience the atmosphere of a 360 degree classroom within the confines of 
his home.<br>
<br>
So whether you are a professional, a housewife, a student or a teacher, Yale 
Open courses promises a course that fits your interest. You can aim for the 
stars by widening your knowledge of Astronomy, drown yourself in the 
soul-stirring poems of Wordsworth with Modern Poetry or ponder over the 
mysteries of life with Philosophy. Open Yale courses gives you a path to choose 
in an elite multi-media environment.<br>
<br>
“Yale has been experimenting with online education for about eight years. In the 
last two years, we decided that the University should focus on capturing several 
dozen of our best undergraduate courses and share with the world for free as our 
contribution to the democratisation of knowledge. That effort dovetails well 
with the University’s general globalisation effort,” said Diana E. E. Kleiner, 
Director of Open Yale courses, and Dunham Professor of the History of Art and 
Classics, Yale University in an e-mail interview to Oman Economic Review.<br>
<br>
Presently, Yale is providing free and open access to seven introductory courses 
taught by distinguished teachers and scholars at Yale University. They are 
Astronomy, English, Philosophy, Physics, Political Science, Psychology and 
Religious Studies.<br>
<br>
Each course includes a full set of class lectures produced in high-quality video 
accompanied by such course materials as syllabi, suggested readings and problem 
sets. The lectures are available as downloadable videos and an audio-only 
version is also offered. In addition, searchable transcripts of each lecture are 
provided.<br>
<br>
The best part about the whole thing is that no enrolment or registration is 
required. All you need is a high-speed Internet connection. Though Yale is not 
the first university to share its prestigious undergraduate courses on the 
Internet, its biggest draw definitely would be the integrated, highly flexible 
web interface. <br>
<br>
The Internet offers varied options in terms of online courses but the Open Yale 
courses offer credibility of the highest level.<br>
<br>
“When an institution of Yale’s stature acknowledges that it is important for 
leading universities to contribute to making educational resources accessible to 
a wider audience, it encourages other institutions to share their intellectual 
treasures as well. This gives online course materials a compelling credibility,” 
the director added. The number of courses is all set to go up in the coming 
years. In the pilot year (2006-2007), the University selected some of its most 
popular undergraduate courses in a wide variety of arts and sciences 
disciplines.<br>
<br>
“We sought introductory level courses on subjects of broad interest taught by 
faculty who have a passion for sharing them with a broader public. We will be 
producing about 30 more over the next several years. For instance, eight 
additional courses are being made and these include subjects and departments we 
didn’t cover in the first year, for example, history, economics and biomedical 
engineering,” the director revealed. Apart from easy access, what makes the 
programme interesting to a learner located thousands of miles away from the 
university is the flexibility it offers.<br>
<br>
According to Professor Kleiner, “Students may audit an entire course or access 
only those parts of courses that they find most valuable. Most important of all, 
the courses are produced under a Creative Commons license, which allows students 
to download, reuse and remix materials as long as the use is educational (not 
commercial) and as long as they acknowledge the faculty member and Yale.”<br>
<br>
The courses, transcriptions and supplementary courseware are produced by The 
Yale Center for Media and Instructional Innovation. They are supported by a 
grant from the William and Flora Hewlett Foundation that ‘seeks to use 
information technology to help equalise access to knowledge and educational 
opportunities across the world’.<br>
<br>
There is no doubt that an online learning environment is very different from a 
standard classroom. Prof. Kleiner agrees but adds that the Yale Open Courses is 
a new concept that almost replicates the normal teaching process.<br>
<br>
“There is nothing that replaces face-to-face interaction between teacher and 
student. Online education is different in kind but we think that Yale has gone 
further than any other university in opening up the classroom online. We have 
used high quality audio to capture the feel of the course taped live and have 
focused on the faculty in such a way that they can become a real presence to 
their online students.” Yale University has also developed partnerships to 
enable these courses to be widely used in different academic institutions around 
the world.<br>
<br>
The American Studies Center in the University of Bahrain will feature segments 
from the Open Yale courses in the Center’s classes. The University of Bahrain’s 
American Studies Center was the first academic program in American Studies 
established in the Middle East and the leading program in the Gulf. In India, 
Yale is working with the Indo-U.S. Inter-University Collaborative Initiative in 
Higher Education and Research’s Amrita satellite network to broadcast courses to 
universities throughout India.<br>
<br>
In China, China Education Television (CETV) has agreed to broadcast individual 
lectures on CETV. CETV broadcasts are viewed by millions of Chinese. Individual 
faculty members at universities around the world will use Open Yale Courses in 
their classrooms. Faculty at the following universities are participating: 
University of Bahrain, Instituto de Tecnologia de Buenos Aires – ITBA 
(Argentina), Fudan University (China), University of Ghana, Jimma University 
(Ethiopia), Tec de Monterrey (Mexico), University of Mumbai (India), Peking 
University (China), University of Tokyo (Japan) and Waseda University (Japan).<br>
<br>
The response to the Yale Open courses has been positive though the university 
hasn’t yet gathered or released hard numbers.<br>
<br>
“What is especially exciting is how many students come from Asia, Europe, South 
America, Mexico, North Africa, the Middle East, etc and that these students are 
of all ages, not just high school and college students, but professionals and 
life-long learners,” said Prof Kleiner.
