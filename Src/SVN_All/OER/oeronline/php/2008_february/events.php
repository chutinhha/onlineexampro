<p align="justify" style="margin-left: 10px; margin-right: 10px"><font face="Verdana" size="1">
<br>
</font><b><font face="Verdana" size="4">Changing people’s lives for better<br>
</font></b><font face="Verdana" size="1"><b>There’s a lot of buzz in the market 
about the BizPro awards. Lawrence Alva, General Manager of NTI, the company 
hosting the awards, speaks to Jayashankar Menon<br>
</b><br>
<b>
<img border="0" src="events.jpg" width="300" height="227" align="left" hspace="8" vspace="6">What does BizPro stand for?<br>
</b>With Oman opening its doors to corporate globalisation,
there is an increasing demand for people with
the right blend of knowledge, skills and attitude.
The need of the day is to focus on developing and
changing the mindset of the people. There is a need
to develop an active, well-informed and independent
workforce that ensures that organisations have
the right people in the right place at the right time
to execute their business strategies. Organisations
do not really accomplish anything, plans don’t accomplish
anything either. Theories of management
don’t matter much. Endeavours succeed or fail because
of the people involved. Hence, it is important
to develop and encourage leadership and life
skills amongst young Omanis, preparing them to be
sound business professionals. To accomplish the
same, the BizPro (business professional) division
was launched in the year 2007.<br>
<br>
<b>When were the awards instituted and what is
the objective behind the award?
<br>
</b>The business skills division of NTI launched this
award in March 2007 with the objective to acknowledge,
encourage, reward and most importantly.
promote the positive achievements of Omani
business leaders and managers. The awards are
entering into their second consecutive year and the
BizPro division is holding another grand event to
present the 2008 BizPro Awards on March 11 at
Hotel Grand Hyatt. The awards will be presented
in two categories, one, for the business leader
and the other for young achievers. The awards
recognise talented Omanis who have displayed
initiative, innovation and the pursuit of excellence
in a leadership capacity. The awardees will become
role models and mentors for their peers and
younger generation. They will also help educate the general public with examples of their achievement.
<br>
<br>
<b>Your objective of instituting the award is
indeed well thought. However, how do you
justify NTI’s footing for such an award?
</b><br>
National Training Institute is the recognised pioneer
training provider in the Sultanate. NTI, after 25 years
of undeterred service, seeks to continue contributing
to the learning and development of progressive
Omani youth and preparing them for the ever demanding
and competitive job market. NTI, with its
vocational training programmes, aims at preparing
and qualifying citizens to meet the State’s requirements
for a qualified national labour force in various
vocational and technical areas. We have trained
over 25,000 Omanis with the skills and competence
required to mingle between training and work, to ensure
that the graduates are qualified, technically and
vocationally, so they can play a considerable role in
increasing efficiency and productivity in the various
economic sectors. We are committed to our corporate
objective of “changing peoples lives for better”
and reinforce our commitment to the development
of Omani youth for a modern economy.
<br>
<br>
<b>How are the awards judged?
</b><br>
Selecting the award winners from a list of accomplished
business leaders is indeed a daunting task.
Under stringent, objective and transparent criteria,
business leaders are nominated by the chiefs of
the media fraternity. The leading personalities go
through a judging process based on a combination
of criteria, including visionary leadership, financial
performance, creativity, innovation, employee relationship
and social responsibility. Young achievers
are selected through a systematic process where the
participants are judged on their management aptitude
and business acumen a day prior to the event. On the evening of the grand finale, 12 finalists will
take part in an exciting and interesting event, flaunting
their business acumen, and three winners of the
Young Achievers Award will be selected by a celebrated
jury comprising senior members of Oman’s
corporate sector.
<br>
<br>
<b>There are so many awards in the market, how
do you differentiate this from the others?
</b><br>
We are in the business of training and developing
people, and know what it takes to win. We have
worked very closely with the employers and have
been very actively involved in contributing to the Omanisation vision of His Majesty Sultan Qaboos
bin Said, who since 1970 has led the country
through a period of remarkable economic growth.
We differentiate ourselves in terms of not only recognising
and applauding the efforts of the business
leaders who have significantly contributed in transforming
the Oman economy but also to foster the
confidence of young Omanis by honing their business
skills through training programmes tailored
to the local needs. After all, developing leaders of
tomorrow cannot be left to chance.
<br>
<br>
<b>Where do we see NTI – BizPro next year?</b> <br>
Focused on delivering knowledge to enable people, realise business value and accelerate technology
returns, we are committed to continuous improvement
in our training standards and practices. We
want our clients to be informed of the latest industry
trends, innovative practices and future opportunities
for their industry locally and from across the world.
Hence, with our tie-ups and accreditations with
world renowned training providers, we look forward
to making BizPro a recognised quality training brand
in the regional market and also the industry’s agile
and dedicated knowledge development partner.</font></p>

