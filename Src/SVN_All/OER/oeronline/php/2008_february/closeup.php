<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
</font><b><font face="Verdana" size="4">Power to the Accessor<br>
</font></b><font face="Verdana" size="1"><b>Though the US FTA provides numerous 
benefits to Bahrain, it is the US which seems to be benefiting thus far<br>
</b><br>
<img border="0" src="closeup.jpg" width="148" height="248" align="left" hspace="8" vspace="6">The US-Bahrain Free Trade Agreement (FTA) has proved to be most beneficial to 
the American side, at least thus far. The deal went into effect at the start of 
August 2006. Available statistics indicate that US exports to Bahrain increased 
by 28 per cent during the first year of the FTA. Conversely, Bahrain’s exports 
dropped by 10 per cent in the same period. But, the agreement has opened the 
doors for Bahrain businesses to tap the available markets in the US, the world’s 
biggest economy, in terms of both imports and GDP.<br>
<br>
According to the US Census Bureau, the value of exports to Bahrain amounted to 
US$529 million from August 2006 to July 2007, up from US$414 million in the same 
period prior to the execution of the FTA. Yet, Bahrain’s exports to the US stood 
at US$638 million from August 2006 to July 2007, down from US$710 million in 
this period earlier.<br>
<br>
<b>Declining surplus</b><br>
Nevertheless, trade balance continues to be in favour of Bahrain though at a 
lower level. The trade surplus amounted to US$109 million in the 12 months 
following the FTA’s implementation versus to US$296 million in the earlier year. 
Undoubtedly, the trade surplus could reverse in the future, partly because US 
firms possess readily available products for export to Bahrain. The vice versa 
is not necessarily true. Searching for an answer to the unexpected result, a US 
diplomat told me that it would be a while before Bahraini establishments learn 
how to do business in the US, notably appreciating the nature of US consumers. 
US consumers care about value of the product and more importantly its after-sale 
service. Bahrain’s exports to the US centre on apparel and clothing accessories, 
aluminium, fertilisers, organic chemicals, mineral fuels and oils and plastics. 
Conversely, US exports to Bahrain focus on aircraft, machinery, vehicles, 
pharmaceutical products, toys, games and sports equipment. Also, US exports 
agricultural produce, notably poultry, snack foods, cotton and processed fruit 
and vegetables. The US is Bahrain’s largest trading partner outside the oil 
sector, where Saudi Arabia remains Bahrain’s largest trading partner by virtue 
of exporting crude oil, which is then processed into petroleum products at the 
country’s sole refinery.<br>
<br>
<b>Removing barriers <br>
</b>The US-Bahrain FTA removes trade barriers related to goods and services, and 
encourages investments between the two countries. However, the US has insisted 
that the FTA could not come in force unless the Bahrain enacts legislation 
extending protection to intellectual property rights together with specific 
penalties against violators. Clearly aware of FTA’s potential in attracting US 
investments, Bahraini authorities opted to accept the US conditions attached to 
its very implementation in the first place. US President George Bush signed the 
FTA with Bahrain into law in January 2006, but the deal went into effect only in 
August the same year. Today, the FTA undoubtedly provides numerous benefits to 
Bahrain, including access to the world’s largest GDP and import market. The US’ 
gross domestic product (GDP) stands at US$14,000 billion. In turn, Bahrain’s GDP 
amounts to less than 1 per cent of the US GDP. Still, according to the US Census 
Bureau, US imports of goods and services amounted to US$2,201 billion in 2006.<br>
<br>
<b>Big deals<br>
</b>Bahrain hopes that some US firms will use Manama as a gateway for doing 
business in regional markets, notably the Gulf Cooperation Council (GCC) states. 
Fortunately, US firms seem interested in grabbing investment opportunities 
available in Bahrain. For one, Kraft of the US is close to commissioning a 
US$40-million facility capable of producing dairy products for local and 
regional markets. GCC laws allow for customs-free entry of goods made in 
Bahrain.<br>
<br>
Again, two US firms have shown interest in constructing the planned causeway 
between Bahrain and Qatar. Yet, others have shown interest in investing in waste 
management and clean energy. By and large, the FTA has proved to be rewarding to 
the US and Bahrain, in terms of trade and investments, respectively. The deal 
has also proved beneficial to the US side. For example, around mid-January, the 
state-owned airline (fully owned by Bahrain), revealed that it planned to 
purchase up to 24 Boeing 787 aircraft in a transaction valued at US$6 billion. 
The deal was announced at the conclusion of a historic visit to Bahrain by 
George Bush (the first by a serving US President). Clearly, the action was 
designed to appease the US side. The first delivery of B-787 will be made in 
2016.<br>
<br>
<b>Business prospects <br>
</b>Booz Allen Hamilton, a US consulting firm, believes that the FTA opens up 
opportunities for six sectors of Bahrain’s economy. These sectors include: 
Textile and apparel, Information and communications technology (ICT), 
Architectural and engineering, Health care, Legal services and Tertiary 
education services. In reality, the sky is the limit when dealing with the US. 
Bahraini firms need to learn how to do business in the US, which should not be 
extraordinarily difficult, given available information about US consumers. The 
most logical development would be that of the Bahraini side making the most of 
the FTA, as the deal provides an uninterrupted exposure to the world’s largest 
economy. <br>
