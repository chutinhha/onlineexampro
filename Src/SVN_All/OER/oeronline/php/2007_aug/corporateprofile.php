<p align="justify" style="margin-left: 10px; margin-right: 10px"><font face="Verdana" size="1">
<br>
</font><font face="Verdana" size="4"><b>Piped growth<br>
</b></font><font face="Verdana" size="1">
<b>Banking purely on customer service, quality and brand image, Muna Noor 
Manufacturing &amp; Trading has registered a steady growth. However, as P Aneel 
Kumar finds out, it has a lot more in the pipeline<br>
</b><br>
<img border="0" src="corp.jpg" width="300" height="200" align="left" hspace="6" vspace="4">For Muna Noor Manufacturing &amp; Trading LLC, the year 2006 was the best ever in 
its 32-year- history. In fact, since 1975, the company has grown by over 200 
times its original strength. Looking forward, the company aims at growing over 
100 per cent annually to achieve a RO30 million turnover in the next five-six 
years. ‘Quality’ growth is what it is targeting. “The company has an excellent 
track record of performance. A consistent focus on quality and service is behind 
our success story,” says Muna Noor general manager K Premkumar. “As part of our 
long-term growth strategy, we aim to double our turnover in the next 5-6 years. 
New investments are also in the pipeline.”<br>
<br>
The company is investing about RO25 million into a new plant that will scale up 
its production capacity to 30,000 tonnes. The new plant, with the latest Germany 
technology, will become operational by September-end. The company has already 
invested RO40 million and has an expansion plan of another RO40 million.<br>
<br>
<b>Quality products<br>
<img border="0" src="corp1.jpg" width="150" height="177" align="right" hspace="6" vspace="4"></b>Largely banking on customer service, quality and brand image, Muna Noor will 
not be wrong if it boasts that it has no match in Oman. However, Premkumar is 
quick to discount this fact. “There are other companies which offer a complete 
range of products. But if you talk about quality and cost-effective products, we 
can say we don’t have any competition in this market,” adds the 30-year-veteran 
with the company.<br>
<br>
The company’s main operations include manufacture of PVC pipes for water supply, 
drainage, irrigation and telecommunication purposes and pipe laying services; 
pest control services; design and installation of automatic irrigation systems; 
import and sale of fittings for PVC systems, valves, plumber’s brassware, water 
heaters, geotextiles and plastic grids, wall and roof claddings, cast iron 
inspection chambers, irrigation equipment, crop cover, insecticides and 
pesticides; aerial spraying of crops.<br>
<br>
Muna Noor exports about 50 per cent of its total production. “About 60 per cent 
of the exports are supplied to Etisalat and Dubai Electricity and Water Supply 
Authority. As the demand grows in the domestic market and we expand our 
capacity, we hope to supply about 50-52 per cent of our production within Oman,” 
says Premkumar.<br>
<br>
Winner of His Majesty’s Cup Award for ‘The Best Five Factories 2006’ in Oman and 
a licensee of the Omani Quality Mark – awarded by the Directorate General of 
Specifications &amp; Measurements – Muna Noor can boast of having undertaken some of 
the top projects in Oman, including the entire infrastructure facilities in 
Shatti al Qurum, 1,000 social housing units in al Khoud, Salalah as also the 
Muscat waste water projects and the Salalah water supply system project.<br>
<br>
<b>Market dominance<br>
</b>Selling under the brand name, Omanplast, Muna Noor’s agency division 
represents a number of world-class products. Electrosteel DI pipes and fittings, 
GSE liners, Terram geotextile, Tensar geogrid are some of its best sellers. 
Keeping in line with Oman’s effort to protect environment, GSE HDPE line has 
variety of uses such as oil sludge containment at oil rigs, sewage or RO plant 
wastewater containment, hazardous or solid waste dumping yards, etc. Tensan and 
Terram geosynthetic products offer economical and easy-to-build solutions in 
construction of retaining walls, reinforced earth foundations, filtration and 
separation application.<br>
<br>
Muna Noor’s irrigation division is a dominant player in the micro, macro and 
landscape irrigation business in the country. Keeping in pace with the 
technological developments, its automation division promotes many speciality 
products in flow control, surge protection, chlorination and SCADA and 
instrumentation, and undertakes system integration projects using custom 
designed software and world-class hardware from Motorola, Siemens, Schneider, 
etc.<br>
<br>
A fully owned subsidiary of Kuwait-based Boubyan Petrochemical Company, Muna 
Noor is now hoping to enter into joint ventures with similar manufacturing 
industries to intensify its export capacity to Africa, the Middle East and 
India. <br>
<br>
Aiming to grow with the nation, the company–originally set up by Mariam Bint 
Abdulmuniem Yousef Al Zawawi – hopes to diligently contribute to the nation’s 
development. <br>
