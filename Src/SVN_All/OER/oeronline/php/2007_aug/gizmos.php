<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
		</font><font face="Verdana" size="4">
iPhone at your fingertips<br>
</font><b><font face="Verdana" size="1">
The Editor-in-Chief of OER has a brand new addition to his collection of 
glittering gizmos. Even as he gets himself acquainted with Apple’s revolutionary 
iPhone, he shares his first impressions on one of the most-awaited and 
much-talked-about launches in the tech world in recent memory</font><font face="Verdana" size="1" color="#CC3300"><br>
</font></b><font face="Verdana" size="1" color="#CC3300">
<br>
</font><font face="Verdana" size="1">
<img border="0" src="giz3.jpg" width="150" height="283" align="left" hspace="6" vspace="4">The long-awaited evolution of the world famous iPod into a phone has taken the 
US by storm with an estimated 200,000 units already in circulation. The 
procedure involved in activating the phone is definitely aimed at the Apple 
converts. You need an Apple email account, especially in the US, with a US 
credit card and even a social security number. In addition, you are tied to just 
one network – AT&amp;T. If you have all of the above, the process is simple: plug 
your phone into your Mac, answer a couple of questions to be assigned a phone 
number and then you are ready to go. If you miss any of the above, your phone 
remains locked with only one function – emergency calls. You can’t even access 
the main menu page!<br>
<br>
Once you get past the initial hurdles and the much-coveted device is activated, 
you are treated to a familiar graphic interface which only Apple can deliver – 
superior high quality 3D animated icons that never seem to tire any pair of 
eyes; a touch screen that allows you to zoom in and out by emulating the shrink 
and expand gesture on the screen; a gyro that knows if you are looking at the 
screen in portrait or landscape; and a scroll feature that gives the pleasant 
sensation of spinning a virtual wheel. It takes a gentle little tab to bring the 
wheel to a halt right under your finger. In effect, it recreates analog real 
world sensations on a 2D screen, and is like space age ergonomics brought to the 
layman!</font><font face="Verdana" size="1" color="#CC3300"><br>
<br>
</font><font face="Verdana" size="1">
iPhone's main features include: <br>
<br>
<img border="0" src="arrow.jpg" width="5" height="8"> Web surfing on Apple’s safari browser. The phone detects unlocked WiFi 
hotspots and inquires whether you want to use it. <br>
<br>
<img border="0" src="arrow.jpg" width="5" height="8"> A Mac email feature that installs itself. Remember you must have a Mac account 
to start the phone. <br>
<br>
<img border="0" src="arrow.jpg" width="5" height="8"> SMS that feels and looks like an online chat room. It displays SMS 
conversations you have had with individuals whenever you decide to SMS them. <br>
<br>
<img border="0" src="arrow.jpg" width="5" height="8"> It has an elegant mapping system similar to map-quest or via Michelin on the 
web. All you need to do is enter your departure and arrival destinations to get 
a map of the route and turn by turn directions to the desired destination. In 
case, you don't know an address, just type the name of the place. Itt scouts for 
the relevant info of the locations in your vicinity on the net and maps it for 
your benefit in a jiffy. You can choose to navigate or even better, just place a 
call.<br>
<br>
<img border="0" src="arrow.jpg" width="5" height="8"> Obviously it has an iPod in it – 4 to 8 GB depending on which model you opt 
for.<br>
<br>
<img border="0" src="arrow.jpg" width="5" height="8"> Built in YouTube browser.</font><p align="justify" style="margin: 3px 10px">
<font face="Verdana" size="1">
<br>
And this isn’t all. There are a total of 16 application icons on the opening 
screen. Although the device lacks a GPS chip/antenna (rumoured to be coming in 
the next generation), it is easier to use compared to Nokia’s GPS-enabled N95. 
In fact, the two phones are trying to do a number of similar things. The N95 is 
superior on the hardware side with its 5 mega pixel camera and built-in GPS but 
Apple blows all competition out of the water when it comes to software – ease of 
use, reliability and ergonomics. <br>
<br>
Having read this far, you are probably thinking “wow”! So let me update you on 
some of the downsides (apart from the ridiculous process involved in acquiring 
and activating the device):<br>
<br>
<img border="0" src="arrow.jpg" width="5" height="8"> The battery life is a joke! If you try to use all the features heavily, as one 
would soon after unpacking the box, it will run out of juice in a couple of 
hours. <br>
<br>
<img border="0" src="arrow.jpg" width="5" height="8"> It uses EDGE instead of 3G so it is slow when you are out of your home WiFI, 
or if you aren’t stealing someone’s bandwidth.<br>
<br>
<img border="0" src="arrow.jpg" width="5" height="8"> You cannot get into the device to change the battery or access the SIM card! 
It is as impenetrable as an iPod.<br>
<br>
<img border="0" src="arrow.jpg" width="5" height="8"> No custom ring-tones, no custom themes, no programmes or games that you can 
add to your desktop...at least not yet.<br>
<br>
<img border="0" src="arrow.jpg" width="5" height="8"> 2-megapixel camera with no flash is poor by today's standards. It performs 
badly in low light.<br>
<br>
All things considered, however, it is fair to say that Apple has made a 
triumphant entry into the highly competitive mobile market. But, unless issues 
related to the battery, accessibility, imaging and 3G and others are addressed, 
the iPhone is going to be overtaken by its competitors sooner than later. For 
now though, if you are an Apple fan, you will surely put up with its 
shortcomings.<br>
<br>
IPhone is not available at a shop near you. The European launch is expected at 
the year-end and the Middle East launch sometime in late 2008.</font><p align="right" dir="ltr" style="margin-left: 10px; margin-right: 10px">
