<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
</font><b><font face="Verdana" size="4">IT partner of choice<br>
</font></b><font face="Verdana" size="1"><b>Olivier Mouries, General Manager, 
GBM Oman talks about the company’s growth so far and the vision to move ahead<br>
</b><br>
<b>
<img border="0" src="market2.jpg" align="left" hspace="6" vspace="4">How has been the growth of GBM during the last couple of years in Oman? <br>
</b>GBM has grown exponentially in the region and particularly in Oman in the 
last few years. In Oman, we grew from 37 staff members in 2004 to 67 at present. 
We have plans to grow more in the coming years keeping sharp focus on quality 
skills. Regionally we have over 600 employees.<br>
<br>
<b>Apart from IBM products, what are the services GBM provides? </b><br>
GBM provides innovative business solutions that range from consultancy to 
deployment, integration and then maintenance. Our unique offerings are based on 
the strength and leadership of more than 40,000 IBM award-winning products, but 
what distinguishes GBM is that we also offer groundbreaking solutions from over 
30 business partners from many parts of the world. No matter what the size of 
the business, GBM provides total service to allow its customers to concentrate 
on their core business while we ensure they have the very latest technology and 
the skills to leverage the maximum benefit from our solutions and our support.
<br>
<br>
<b>How many branches do you have in Oman? <br>
</b>At the moment we have one branch in Qurum, from which we service the entire 
Sultanate. We are monitoring closely the possibility of an additional branch in 
Sohar.<br>
<br>
<b>How is GBM gearing up to tap Oman’s growing telecom and IT sectors?<br>
</b>GBM has been an active partner in information technology and 
telecommunications in Oman since 1990. It is a very active business segment 
where we have recently delivered some critical projects. Clearly, GBM thought 
leadership in this area can assist in supporting the tremendous growth and need 
of diverse services that this industry wants to achieve. We are constantly 
looking at innovative ways to assist the leadership in developing the country 
into a regional showcase in the field of ICT. <br>
<br>
<b>In what ways does GBM support business in Oman? <br>
</b>At GBM, we serve all industries and businesses equally. A customer comes to 
us knowing we are trustworthy and committed to provide strong local support, 
which is something we maintain very seriously. They also know we will not only 
meet their expectations, but surpass them and ensure they are ready to excel in 
their business for years to come. We look on this approach as a strong, 
value-add partnership, rather than that of a supplier and a buyer. We believe 
that this is what distinguishes us from the market.<br>
<br>
<b>What is your philosophy on corporate social responsibility? </b><br>
As I have said, GBM looks for partnerships rather than one-off sales, and we 
understand that partnerships extend beyond just the hard business aspects. In 
Oman we have donated a Linux Laboratory to the government to enable young Omanis 
to further their expertise in IT. We have plans for other initiatives in future, 
specifically in developing the local IT skills. Moreover, we lose no opportunity 
in bringing information on the latest developments in the IT sector to the 
public through seminars and presentations.<br>
<br>
<b>What is the share of Oman in GBM’s overall Gulf market? <br>
</b>GBM operates throughout the Gulf and Oman is a steady contributor to the 
overall growth of the organisation. We encourage healthy competition as this 
ensures the customers get the best service at the best price, but we are not 
just market share driven. We believe in long-term relationships built on trust 
and our own partnership approach, and that is how we have steadily grown in the 
region since GBM was formed in 1990.<br>
<br>
<b>What software support do you provide for anti-money laundering practices? <br>
</b>Anti money laundering and operational risk solutions form a very active part 
of the GBM portfolio, not only in Oman but throughout the region. The rush to 
Basel II compliancy has ensured that there are many AML and OR solutions in the 
market. Our financial customers here are very selective in choosing the right 
product to gain them compliancy but they still want something more. That is why 
we have created the most popular AML and OR solution in the Gulf, called 
RiskSecure.<br>
<br>
<b>What are your future plans?</b><br>
We are still expanding our offerings in the country and year on year we have 
increased in size and will continue this into the near future. We intend to show 
our value add to customers by offering them solutions that not only address 
their immediate needs, but which also anticipate their requirements for the 
coming years ensuring they get the best possible return on their investment. We 
will grow with the country and continue to seek to be the IT partner of choice. </font>
