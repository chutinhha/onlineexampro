<p align="justify">
<b><font face="Verdana" size="4"><br>
</font></b><b><font face="Verdana" size="4">The Natural Touch</font></b><font face="Verdana" size="1"><br>
<b>Oman tourism has managed to differentiate itself from others in the region by 
capitalising on its diverse landscape, says Joseph Smart</b><br>
<br>
<img border="0" src="economy.jpg" width="163" height="451" align="left" hspace="6" vspace="4">Despite its big tourism potential, Oman has not followed the standard strategy 
of erecting large-scale luxury development projects to pull in visitors. Though 
luxury developments are on the rise – both literally and figuratively – Oman has 
managed to differentiate itself from others in the region by capitalising on its 
diverse landscape. Ecologists, divers and nature lovers alike embrace the fact 
that Oman is looking to protect its natural sites.<br>
<br>
Oman’s Ministry of Tourism is particularly committed to conservation; in 
particular to minimising the negative impact of human development on the natural 
landscape. The Sultanate is blessed with rugged mountain ranges, pristine 
beaches, an abundance of historical sites, not to mention world-class diving and 
an alluring desert. With a 1,600-km coastline and diverse marine life, Oman is 
perfectly placed to draw in nature lovers, trekkers and tourists seeking the 
sun, sand and sea. Promotion unsurprisingly focuses on the diversity on offer, 
spanning adventure, environmental and geological tourism. This runs in parallel 
with conventional resort tourism widely found elsewhere in the Gulf. The 
Sultanate is also widely regarded as the friendliest state in the Gulf, with 
locals known for their hospitality.<br>
<br>
<b>Fast-expanding sector</b><br>
Despite the country’s potential as a tourism destination, Oman’s Ministry of 
Tourism emerged as independent ministry only in 2005. It has refrained from 
measuring success purely in terms of visitor numbers, taking instead a more 
selective approach by refusing to sacrifice its natural beauty for sprawling 
should-to-shoulder tourism development. The fact that statistics on tourist 
arrivals are hard to come highlights the importance that the country attaches to 
large increases in yearly arrivals. With a population of 3.2 million, the 
Sultanate does not want its territory swamped by camera-totting visitors.<br>
<br>
An expanding tourism sector nonetheless allows Oman to broaden the base of its 
economy while creating jobs for a growing workforce. Currently 37 per cent of 
all employees in the tourism sector are Omanis – a figure that the government 
wants to increase to 80 per cent by 2010. Doing so is something of a necessity 
given that Oman’s oil and gas reserves are expected to run dry within the next 
two decades. The tourism sector grew by as much as 16 per cent in 2006 and is 
set to continue expanding in 2007. </font></p>
<p align="justify">
<font face="Verdana" size="1">Thus, even though Oman is not looking for glitz, as is the case with Dubai, the 
Sultanate is indulging in a number of lavish new projects. Notable in this 
regard is the Blue City project, a self-contained metropolis on the coastal 
region at Al Sawadi. Apart from retail centres, golf courses and four hotels, 
the project will include 200 villas and 5,000 apartments. Similarly, the Al 
Sifah and Salalah projects – both residential and seafront resorts – are 
expected to increase Oman’s share of the regional tourism pie, both in terms of 
visit from within the GCC and from beyond.<br>
<br>
</font>
<img border="0" src="economy1.jpg" width="250" height="204" align="right" hspace="6" vspace="4"><font face="Verdana" size="1">Interestingly, the profile of tourists that visit Oman has changed over the 
years. Though the bulk of tourist arrivals have traditionally come from the Gulf 
region, the number of GCC tourists has now fallen to less than half of all 
arrivals, with European visitors compensating for the shortfall. In fact, there 
has been a notable increase in British and German visitors entering the 
Sultanate. Tourism growth though continues apace, registering an average of 
seven per cent per annum. While some 290,000 visitor arrivals were registered in 
1994, the number increased to 503,000 by 1999, and surged to 1.1 million in 
2002.<br>
<br>
Oman’s tourism sector contrasts with that of the United Arab Emirates. Of 
course, each emirate has its own target market. As the biggest commercial and 
tourism hub of the GCC region, Dubai rides on an effective combination of water 
sports, shopping attractions, sun, sea and sand, not to mention high-profile 
sporting events. Luxury housing projects also feature among its attractions. In 
fact, it remains the most popular weekend destination for fellow GCC citizens. 
The city hopes to attract 6.8 million tourists in 2007 and 15 million in 2010. 
The rest of the Emirates have looked to forge their individual identities. 
Sharjah sells itself as a cultural hub and the family destination in the UAE. 
Ras al-Khaimah (RAK) is in the meantime developing its coastal resorts and is 
hoping to net two million visitors by 2010 following the erection of 20-25 new 
hotels. The rest of the Emirates too are trying to claw away some GCC travellers. </font></p>
<p align="center">
<img border="0" src="economy2.jpg"></p>
<p align="justify">
<font face="Verdana" size="1"><b>Creating a niche</b><br>
Oman is in some ways similar to and in some ways different from the UAE. It 
targets both the international and local GCC market, like Dubai, but it focuses 
less on big-hitting events and shoppers. Like some of the less popular Emirate 
states, Oman focuses on holidaymakers in search of peace and quiet – even though 
it has far more to offer in terms of beauty and diversity. Its increased 
popularity as a holiday destination comes not just from the increasingly 
effective promotion and marketing strategies deployed by the Omani government, 
but also from the demand pressures in Dubai. Consider, for instance, the steep 
room rates and a shortage of accommodation elsewhere in the region.<br>
<br>
Oman’s position as a tourism destination in the GCC is also reflected by the 
level of investments being made. The UAE leads the pack in the GCC in terms of 
investments in tourism-related projects, accounting for AED 858 billion worth of 
investments of the total of AED 1 trillion being invested in the region (this 
includes investments in projects that are expected to be completed by 2018.) In 
comparison, investments in Oman totalled AED 61 billion, a distant second, 
followed by Qatar at AED 31.1 billion, according to a survey by Reed 
Exhibitions. The high level of investment in Oman points to the confidence that 
rests in the country’s tourism industry.<br>
<br>
It is little surprise that Oman increasingly occupying a more prominent place on 
the world tourism map, even if it is regarded as something of a more exclusive 
destination for international travellers. Oman is, of course, a relative 
newcomer, with public and private investments in the sector amounting to a 
little more than US$5.2 billion over the last decade. But, since its 
establishment two years ago, the Ministry of Tourism has been actively pushing 
for controlled growth, through investments in infrastructure projects, 
liberalising laws on property ownership and assisting operators through 
sponsorship of events as well as joint ventures and international tourism fairs. 
Visitors from more than 60 countries can now acquire visas on arrival. <br>
<br>
Over the next five years a doubling of capital expenditure is expected. High-end 
hotels and resorts will get the majority of this, with adventure tourism also 
benefiting. Though Oman is investing in large-scale tourism projects, the 
Sultanate is not likely to shed its commitment to conservation and the 
environment – both of which count as niche areas. Oman’s natural beauty will 
continue to attract visitors from the Europe and the Gulf alike. <br>
&nbsp;</font></p>

