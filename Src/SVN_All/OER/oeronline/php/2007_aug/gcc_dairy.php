        year, making it the largest and one of the fastest growing markets in 
        the region. The glaring exception to this robust market growth has been 
        broadband internet, which currently reaches less than 1 per cent of its 
        24 million residents. But with three new fixed line operators expected 
        in 2008, EFG-Hermes is projecting broadband to be rolled out in a 
        dramatic fashion by 2010. EFG-Hermes said demand for high bandwidth 
        connectivity is rising from the business sector and households. “In 
        2010, we expect broadband penetration to reach 16 per cent.”<br>
