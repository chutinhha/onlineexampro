        <b>Kuwait’s surplus declines in 2006-07</b></font><font size="1" face="Verdana"><br>
        Kuwait posted a surplus of US$13 billion, during 2006-07 down from last 
        year’s US$19.14 billion, reveal a report by the country’s finance 
        ministry. The commitments of funds and actual expenditure rose to US$41 
        billion from the preliminary estimate of US$34 billion in this period. 
        Revenue projections, based largely on oil exports, were almost unchanged 
        at $54 billion. The 2006-07 surplus was lower than the previous year 
        because the government made a one-time payment of around US$6.97 billion 
        to a social security fund to cover overdue contributions. Kuwait’s 
        surplus oil wealth is invested by the state-owned Kuwait Investment 
        Authority, which had at least US$213 billion in assets under management, 
        on March 31.</font></p>
