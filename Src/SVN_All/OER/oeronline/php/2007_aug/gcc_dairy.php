		</font><b><font size="4" face="Verdana">Internet influencing consumer 
        behaviour</font></b><font face="Verdana" style="font-size: 14pt"><br>
        </font><font size="1" face="Verdana"><b>Around 55 per cent of internet 
        users across the Middle East and North Africa regularly frequent blogs, 
        social network sites, email discussion groups, community boards and 
        other web-based sources, according to a research carried out by leading 
        job site Bayt.com. The survey, conducted in conjunction with market 
        research company YouGovSiraj, revealed that rising internet use and its 
        increasing penetration in the region is having a profound effect on 
