        <b>Qatar unifies financial regulatory system</b></font><font size="1" face="Verdana"><br>
        Qatar has decided to unify its financial regulatory system, broadening 
        the international standards championed by the recently formed Qatar 
        Financial Centre. The unified regulator, scheduled to start work in 
        early 2008, consists of the central bank’s banking supervision 
        department, the newly formed regulator of Doha’s stock market and the 
        QFC, which has attracted about 50 international companies since 2005. 
        With this, local banks, brokerages, insurance and asset management firms 
        will have to come into line with a tighter regulatory structure by about 
        2010. Qatar has 25 banks, insurance firms and brokerages, most of which 
        are enjoying stellar profits given the oil boom in the Gulf, making 
        change easier to effect. Tighter regulations standards are already 
        arriving through self-regulation and the introduction of Basle II 
        standards.</font></p>
