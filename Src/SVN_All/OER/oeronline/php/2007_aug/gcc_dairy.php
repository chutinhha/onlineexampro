        An additional 22,000 hotel rooms are expected to be available in Dubai 
        by the end of 2008 and this is seen helping the fast-growing tourism 
        destination overcome part of its room shortage. Dubai had a total of 415 
        hotels and furnished apartments last year, offering about 40,000 rooms 
        in various categories, according to the Department of Tourism and 
        Commerce Marketing (DTCM). Dubai hotels received about 6.5 million 
        guests in 2006 compared with 6.1 million a year before. The tourism 
        department projects the number of hotel rooms to increase to 93,867 in 
        2016 when 31 properties planned as part of the Dh100-billion Bawadi 
        project in Dubailand will be ready. </font></p>
