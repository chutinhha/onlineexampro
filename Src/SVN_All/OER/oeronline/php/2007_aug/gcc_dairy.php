        <b>Maktoob to establish online Arabic social network</b><br>
        Maktoob.com has partnered with Faye3.com, the winner of this year’s 
        Queen Rania National Entrepreneurship Competition (QRNEC), in an effort 
        to create the largest social network in the Arabic-speaking world, one 
        that caters to the likes and tastes of Arab web users. Samih Toukan, CEO 
        of the Maktoob Group, said: “social networking is an emerging phenomenon 
        and today, the web is enabling everyone to create their own community 
        based on their interests and locations. The platform aims at providing 
        the tools for these active people.”<br>
        <br>
        The Queen Rania National Entrepreneurship Competition (QRNEC) is an 
        annual competition that invites teams from different universities to 
        present a business plan for a technology-based entrepreneurial project. 
        The panel of judges includes specialists from Cambridge, MIT, and from 
        leading global companies. The Faye3.com team came in first ahead of 92 
        participating teams this year. <br>
        <br>
        <b>
        <img border="0" src="gcc1.jpg" width="250" height="167" align="right" hspace="6" vspace="4">Gulf cos raise $105bn in H1 </b><br>
        Driven by the regional economic boom, Gulf companies raised US$105 
        billion during the first six months of this year, according to a report 
        by Thomson, an international information service provider. “Fast-growing 
        companies need capital and are turning towards the bond and loans 
        markets for cash. Mergers and acquisitions and initial public offerings 
        have also fuelled the leap,” the Thomson report informed.<br>
        <br>
        Thomson’s noted that using the five popular financial instruments of 
        IPOs, M&amp;As, bonds, project finance and corporate loans, the capital 
        raised by GCC companies increased by 25 per cent from year-ago levels. 
        More corporate borrowers are turning to bonds or sukuk as a means of 
        raising finance. Project finance reported a sharp decline while M&amp;A 
        activity originating from the region in the first half exceeded that 
        seen in whole of last year. The big US banks pretty much cornered most 
        of the big-ticket deals, with the local banks remaining on the fringes.<br>
        <br>
        <b>ME pharma, biotech market worth $28bn</b><br>
        The Middle East market for pharmaceutical manufacturing and 
        biotechnology is valued at over US$28 billion, according to the 
        organisers of the region’s premier industry event ‘Pharmaceutical 
        Manufacturing and Biotechnology Middle East (PABME)’. With a 
        double-digit annual growth, the market looks set for even stronger 
        growth as some of the world’s major players eye the regional market. An 
        estimated 450 pharmaceutical manufacturers already operate in the Middle 
        East, a market valued at over US$ 18 billion and growing at more than 10 
        per cent per annum. The regional biotechnology market is smaller (US$10 
        billion) than pharmaceutical manufacturing but is growing at an even 
        faster pace, at 15-18 per cent per annum.<br>
