        <img border="0" src="gcc2.jpg" align="left" hspace="6" vspace="4"></font><font size="2" face="Verdana"><b>Dubai becomes the tallest
        </b> </font><font size="1" face="Verdana"><br>
        Burj Dubai – at 512.1 metres (1,680 ft) – developed by Emaar Properties 
        has become the tallest building in the world, at the time of going to 
        the press. Burj Dubai is now taller than Taipei 101 in Taiwan, which at 
        508 metres (1,667 ft) held the tallest-building-in-the-world title since 
        it opened in 2004. Burj Dubai has now reached 141 storeys – more storeys 
        than any other building in the world. On schedule for completion in 
        2008, Burj Dubai will be the tallest structure in the world in all four 
        of the criteria listed by the Council on Tall Buildings and Urban 
        Habitat (CTBUH). The council measures height to the structural top, the 
        highest occupied floor, to the top of the roof, and to the tip of the 
        spire, pinnacle, antenna, mast or flag pole.<br>
        <br>
        During its construction, Burj Dubai has left behind the skyscrapers that 
        previously defined tall tower architecture around the world, such as 
        Petronas Towers in Malaysia (452 metres, 1,483 ft); Sears Tower, Chicago 
        (442 metres, 1,451 ft); Jin Mao Building, Shanghai (421 metres, 1,381 
        ft) and Empire State Building, New York (381 metres, 1,250 ft). When 
        completed, Burj Dubai will have 56 elevators travelling at 1.75 to 10 
        metres/sec and double-decker observatory elevators that can carry 42 
        people at a time.<br>
        <br>
        Emaar Properties has partnered with leading consultants such as South 
        Korean construction major Samsung Corporation and New York-based Project 
        Manager Turner Construction for the project. The US$20-billion 500-acre 
        downtown development is billed as the most prestigious square kilometre 
        on earth. Burj Dubai will feature residential, commercial and retail 
        components including the world’s first Armani Hotel &amp; Residences, 
        exclusive corporate suites, a business centre, four luxurious pools and 
        spas, an observation platform on Level 124 and 150,000 sq ft of fitness 
        facilities.<br>
