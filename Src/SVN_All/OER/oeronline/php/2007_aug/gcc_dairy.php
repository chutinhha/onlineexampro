        <b>IMF: Gulf oil producers should maintain dollar-peg </b><br>
        The International Monetary Fund ha said that Gulf oil producers, 
        including Saudi Arabia and the United Arab Emirates, should keep their 
        currencies fixed to the dollar. “A fixed exchange rate is best for these 
        oil producers,” said Mohsin Khan, IMF’s director for the Middle East and 
        Central Asia. “Their main export is priced in dollars, it makes it 
        easier for them to hedge.” Recently Morgan Stanley had stated that 
        Middle East oil producers might face pressure to revalue their 
        currencies to narrow the surpluses. “The analytical framework for 
        deciding on fundamental misalignment for oil producers is lacking, 
        because some of these countries are running surpluses through no fault 
        of their own,” said Khan. Some time back, IMF had said that it requires 
        all 185-member countries to reject practices that create instability for 
        other nations, such as “currency misalignment.” </font></p>
