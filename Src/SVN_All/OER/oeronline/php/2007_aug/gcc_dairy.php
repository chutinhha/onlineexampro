        </b>
        <br>
        <img border="0" src="gcc.jpg" width="250" height="167" align="left" hspace="6" vspace="4">Around 55 per cent of internet users across the Middle East and North 
        Africa regularly frequent blogs, social network sites, email discussion 
        groups, community boards and other web-based sources, according to a 
        research carried out by the leading job site, Bayt.com. The survey, 
        conducted in conjunction with market research company YouGovSiraj, 
        revealed that rising internet use and its increasing penetration is 
        having a profound effect on regional consumer behaviour. <br>
        <br>
        Of 4,600 Bayt.com users – both males and females – polled, an 
        overwhelming 81 per cent admitted that the internet has made them more 
        favourable towards certain brands. ‘The results should serve as a 
        wake-up call to anyone in this region who has an interest in marketing 
        and media,’ said Rabea Ataya, CEO of Bayt.com. “Our latest study marks a 
        pioneering step in understanding how people in the region use the 
        internet when making their buying decisions.” The study reported that 
        the internet currently ranks second to word-of-mouth when it comes to 
        influencing people’s purchasing decisions, with 43 per cent saying they 
        used websites as a source of information as compared to 55 per cent who 
        relied on people they knew. Newspapers came in third with 40 per cent. 
        Again, the internet proved to be an important source for research, with 
        76 per cent agreeing that they sought more information on a service or 
        product on the web after seeing it advertised in other media. Online 
        advertising also impacted in-store behaviour, with 64 per cent agreeing 
        that they often checked out a product in the store after noticing a 
        web-based advertisement. <br>
        <br>
        Ringing the warning bells for the traditional media, 47 per cent said 
        they used the internet for more than three hours per day, compared to 
        only 18 per cent for television viewers. In Kuwait, 54 per cent of 
        internet users spent more than three hours a day online. Overall, there 
        was a positive feeling regarding the impact of internet on purchases, 
        shopping behaviour and attitudes. The internet made 83 per cent aware of 
        a new brand while 68 per cent later bought the brand. Again, the 
        internet influenced 59 per cent users to change brands. <br>
        <br>
        “One of the key findings of this study is that people trust their own 
        abilities to use this medium to inform their purchasing decisions,” said 
        Ataya. ‘More than a third of respondents regarded themselves as having 
        expert internet capability, while 47 per cent described themselves as 
        knowledgeable. This is a sign that consumer control is really taking 
        off.” Ataya added the survey revealed the internet goes beyond informing 
        purchasing decisions. Some 58 per cent of respondents, for instance, say 
        they have bought items online. For GCC Arabs, the figure was 52 per 
        cent, for the UAE 49 per cent, and for Egypt 25 per cent. <br>
        <br>
        In terms of the kinds of online purchases, travel came out on top, with 
        41 per cent of respondents making online travel-related transactions 
        over the past 12 months, followed by software and books at 40 per cent. 
        Furniture purchases ranked the lowest, at 4 per cent. For services, 42 
        per cent said they had found a job online, while 39 per cent said they 
        paid their utility or other bills. The level of satisfaction in terms of 
        online transactions was high, with 58 per cent stating they were 
        satisfied compared to 14 per cent expressing dissatisfaction. But there 
        were barriers to online purchasing. The survey found that 62 per cent of 
        respondents claimed they would prefer to see a product prior to 
        purchase, while 45 per cent cited the fact they didn’t have a credit 
        card as the reason for not buying online. Such people, the study 
        suggests, use the internet primarily for research and make their 
        purchases in-store. <br>
