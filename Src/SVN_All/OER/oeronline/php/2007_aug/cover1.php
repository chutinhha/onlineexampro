</font>
<font face="Verdana" size="4">
Key to Sultanate’s economy<br>
</font><font face="Verdana" size="1">
<b>The development of Duqm is being overseen by the Ministry of National Economy 
and the Ministry of Transport and Communications. In this interview with P Aneel 
Kumar, H.E. Ahmed bin Abdul Nabi Macki, Minister of National Economy and 
Vice-Chairman of the Financial Affairs and Energy Resources Council, says Duqm 
would be an important centre for diversification of the nation’s economy</b><br>
<br>
<b>
<img border="0" src="cover3.jpg" width="175" height="205" align="left" hspace="6" vspace="4">Duqm is considered as the next industrial and commercial hub of Oman. How?<br>
</b>The development of Duqm and surround area will create a major new city in 
the Al Wusta region that will serve as an administrative, industrial and 
commercial centre. The government is developing the necessary infrastructure and 
facilities required (e.g. schools, hospital, airport, telecom) plus the 
amenities necessary to attract companies, service providers and people to what 
will be an important centre for continuing the development and diversification 
of our economy. This major new city will bring employment and increased 
prosperity to Oman in general and to the Al Wusta region in particular.<br>
<br>
<b>What are the main projects that may come up at this place?<br>
</b>Initially the main projects are those being undertaken by the government, 
the development of a town, including the provision of administrative and social 
services, port and airport and fishing harbour plus electricity and water. The 
government with our Korean Partners is developing a world-class ship repair 
facility with very large graving docks within the Duqm port area. The 
ship-repair facility is expected to encourage the development of a range of new 
ventures that will provide specialist services, fabrication, repair, services, 
etc.. As you are aware the government is considering the development of a major 
new integrated refinery and petrochemical complex that can be expected to bring 
a range of specialist downstream processing projects to the region, as has 
already happened in Sohar. Duqm also has prolific marine resources and it is 
also expected it will become a major centre for the processing and exporting 
fish and fish products. It is also planned that Duqm will be the centre for the 
export and potentially processing of industrial minerals, limestone, etc.<br>
<br>
<b>What is the size of investment, both by the government and the private 
sector, expected in the region?<br>
</b>It is very difficult to provide an accurate assessment, but in the medium to 
long term it is expected that private and government expenditure will be in the 
billions of Omani rials. Duqm is expected to grow into a town of more than 
50,000 people and almost everything is yet to be built – by government, 
companies and the public. The cost of the development of the town and 
infrastructure can be expected to be very significant. The development of the 
city of Duqm is a major undertaking and it will be the first time in Oman’s 
recent history that a completely new city has
been created.<br>
<br>
<b>Which are companies that might set up their base there?<br>
</b>The first major company to be based in Duqm will be Oman Dry Dock Company, 
which is expected to start operations in mid-2010 and employ up to 3,000 people. 
Other local and international companies are considering projects at Duqm but 
their names cannot be released at this time.<br>
<br>
<b>What are the concessions or rebates extended for companies that want to set 
up their base here?<br>
</b>The Master Plan for the Duqm area sets aside large areas reserved for heavy 
industry, a free trade zone, an area for light industry plus areas reserved for 
tourism and commercial developments. Companies setting up in Duqm will be 
offered the same attractive terms and conditions as apply elsewhere in Oman.<br>
<br>
<b>How different will be Duqm from other cities like Muscat, Salalah and Sohar?</b><br>
The city of Duqm is the first almost totally new major town to be developed in 
Oman. Its design will be modern and take into account best practices in town 
planning but it will still retain a distinctly Omani character, which reflects 
the values and traditions of Oman.<br>
<br>
<b>What is in store for exporters?</b><br>
Until recently there was very little development in Duqm and a low population 
density in the area. The planners involved in preparing the Master Plan have 
been able to optimise the layout of the industrial areas, town, etc. so as to 
best take advantage of access to both the large modern port and airport being 
developed at Duqm. We are confident that the developments in Duqm will provide 
exporters, whether they are involved, for example, in bulk chemicals or fish 
products, with access to good export facilities. These facilities, when combined 
with access to raw resources and attractive fiscal and other conditions, should 
provide many opportunities for exporters.<br>
<br>
<b>Could you give the details of the infrastructure facilities coming up in the 
area?<br>
</b>Duqm Port is a major new facility with a turning basin of more than two 
kilometers diameter that will comprise the ship-repair facilities, bulk and 
general cargo plus facilities for the ROP Coast Guard, Royal Navy of Oman, etc. 
The port is expected to be ready by 2010. The airport is a full international 
sized project designed for passengers and air cargo and will be capable of 
accepting heavy lift aircraft. Preliminary design has commenced and the airport 
is expected to be ready for service in 2010. The airport is expected to cost 
approximately RO55-60 million. In addition, there will be schools, hospital and 
other infrastructure facilities required by a modern city including power 
station and desalination plant.<br>
<br>
<b>How many tenders have been issued so far and how many contracts have been 
awarded?</b><br>
To date, a large number of contracts have been issued for various projects and 
studies. The first major construction tender awarded was for the marine works 
for the port. The contract was awarded to a consortium of CCC, JDN and STFA. The 
contract was for approximately RO187 million. The tender for the construction of 
the ship-repair facilities has been let, but is not yet closed. The Rural Areas 
Electricity Company has let a tender for the construction of a new power station 
and desalination plan located close to the port to meet the initial electricity 
and water demand of the town and port plus dry dock. The expected cost of these 
facilities is estimated to be in the range of RO15-20 million. The government is 
also studying options, which include the construction of a large power and 
station and desalination plant for meeting the long-term supply of electricity 
and water to the area.<br>
<br>
<b>What is the roadmap for the development of the area?</b><br>
The Supreme Committee for Town Planning has had a Master Plan for the 
development of Duqm prepared by international consultants in consultation with 
the relevant ministries and other authorities. This Master Plan provides a 
detailed plan for the development of the city, its infrastructure and industrial 
areas.<br>
<br>
<b>What is the uniqueness of this town in attracting investments?<br>
</b>Duqm is strategically located and, unlike the majority of the Gulf region, 
enjoys a relatively mild summer. There are also many attractive tourist sites 
within the region. This, when combined with access to raw resources, good 
infrastructure and a well-planned city and the industries, is expected to 
encourage the rapid development of this town and the industries to be 
established. The ship-repair facility is also expected to attract other 
industries to the area. We are seeing strong interest from local, regional and 
international investors.<br>
<br>
<b>What will be the overall impact of this development on the economy?<br>
</b>The development of Duqm is expected to make a significant short and longer 
term contribution of Oman’s economy. There will obviously be significant 
expenditure and employment created during the construction of the 
infrastructure, town and industries and potentially, significant foreign direct 
investment. In the medium to longer term, the new industries will generate 
substantial revenues and export income and create significant direct and 
indirect wealth and employment. The Duqm development is also expected to help 
further diversification of the economy.<br>
