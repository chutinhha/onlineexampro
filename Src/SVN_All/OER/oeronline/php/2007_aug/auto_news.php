        </font><b><font face="Verdana" size="2" color="#CC3300">Traction control 
        with Hummer H3<br>
        <img border="0" src="auto9.jpg" align="right" hspace="6" vspace="4"></font></b><font face="Verdana" size="1">Powered by an advanced, fully 
        integrated powertrain system, the Hummer H3 now comes with full-function 
        traction control. The midsize H3 is suited for city driving and is 
        off-road capable. According to a release from the company, the H3’s 
        advanced traction control system was designed to tame the raw power 
        rippling beneath its metal skin. When on slippery road surfaces, 
        traction control continually monitors the rotational speed of each 
        wheel. If one wheel is spinning more than the others, traction control 
        applies braking pressure to the spinning wheel to help it regain its 
        composure and transfers available torque to the wheels that have 
        traction. Using this system, the Hummer H3 with three spinning wheels 
        can still move forward, even up a 10 per cent incline, the release 
        added.</font><hr width="90%" size="1">
