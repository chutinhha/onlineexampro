        and 10 cars per week are now tested in the crash test laboratory – one 
        of the leading facilities of its type in the world. According to a 
        company release, the Volvo Cars Safety Centre is designed to reproduce 
        accidents of many different kinds. The crash test laboratory is equipped 
        with two tracks, one movable and one permanent. The movable track can be 
        adjusted up to 90 degrees to enable tests of all kinds, from frontal to 
        side collisions, between cars travelling at different angles and speeds. 
        The permanent track is long enough to enable the cars to be accelerated 
        to speeds up to 120 km/h. A series of other tests, such as rollover 
        accidents or collisions with animals or other objects in the surrounding 
        environment, can also be performed.</font><hr width="90%" size="1">
