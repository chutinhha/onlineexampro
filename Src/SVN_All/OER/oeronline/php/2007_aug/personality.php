<p align="justify" dir="ltr" style="margin-left: 5; margin-right: 5"><b>
<span style="font-size: 14pt"><br>
</span></b><b><font face="Verdana" size="4">Son of Sharqiya Soil</font></b><font face="Verdana" size="1"><br>
<b>Mohammad Al Farei, the young and dynamic GM of TBWA\Zeenah, is not one to 
rest on his laurels and is forever seeking to make a difference<br>
</b><br>
<img border="0" src="person1.jpg" align="left" hspace="6" vspace="4">A lean frame with a pencil-line moustache in spotless white Omani robes. Add a 
fair dose of an ever-smiling visage to that combo, and you have Mohammad Al 
Farei, General Manager, TBWA\Zeenah. The 1977-born business management graduate 
from Warwick University in the United Kingdom has had a dream run right from the 
word ‘go’. <br>
<br>
After completing his education from the Sharqiya region (secondary schooling at 
Samad, higher at Mudabi and a diploma in business administration from Ibra) in 
1997, Farei packed his bags for Muscat for a job interview at the Zubair 
Corporation. The company was scouting for an executive assistant to the 
chairman. It was the only job Farei applied for. There has been no looking back 
since.<br>
<br>
“That’s where my real education began. That’s where I began to understand the 
world as we know it,” says a satisfied Farei, seated in his spacious office in 
Madinat Ilam. And if one sees all that Farei has achieved, the Chairman was 
certainly a great teacher. What did the young and nubile Sharqiya-ite learn? 
Vision, patience and humility. “It was important to remain patient to succeed in 
life. In any work environment, provocations are many. The key is to retain your 
poise. That was lesson number one I learnt,” he elaborates. <br>
<br>
When the Bait Al Zubair Museum project kicked off in 1997, Farei was given the 
additional responsibility of bringing it to fruition. It was a short stint as he 
packed his bags yet again for the UK to continue his studies. “His Excellency 
kept a close watch on my progress overseas,” declares a proud Farei. <br>
<br>
On his return in 1999, Farei was named manager to the board of directors of the 
Zubair Corporation, becoming the communication link between the board and the 
group companies. Farei recalls the period since then as a whirlwind trip. “I had 
a ringside seat, watching a successful organisation being run and how it 
communicated with its stakeholders.”<br>
<br>
<b>Rising to the Challenge</b><br>
Around 2003, Zeenah was formed, with Farei being in the core team. “The board 
felt that there was no uniform communication from the group companies and the 
work carried out by external agencies were not up to the board’s satisfaction. 
That’s when, the question of ‘why not?’ popped up and resulted in Zeenah,” says 
Farei. <br>
<br>
From a three-member team, TBWA\Zeenah has grown into a 40-plus advertising 
agency. Does he feel handicapped heading the unit, considering that he does not 
possess any advertising background? “Not at all. Professionals with relevant 
experience are very much on board. My role is that of an administrator and 
strategising future growth.” <br>
<br>
Farei believes that a lot more can be achieved on the creativity front and adds 
that things will change in the years to come as “consumers get exposed to all 
kinds of media, and industry (advertisers) will have no option but to satisfy 
those informational needs as well.” However, he refuses to be drawn into a 
discussion about the arrangement with TBWA, except declaring that it is a 
“partnership beyond simple affiliation”, and it gives Zeenah access to the 
global giant’s intellectual and management expertise. <br>
<br>
<b>Communication skills</b><br>
TBWA, Zeenah relationship is shining. The “partnership” has helped them manage 
Zubair Automotive, Bank Muscat, Etihad Airways, Shangri-La’s Barr Al Jissah 
Resort &amp; Spa, Oman German University (OGTech), Henkel, PRD Nationwide, among 
others. Farei is confident that business is growing and is therefore gearing up 
for expansion. <br>
<br>
But it is his ability and success in managing a multi-cultural environment that 
gives him the most joy. “People of 13 nationalities are working in TBWA/Zeenah. 
You know the pressure when people drawn from such diverse backgrounds jam 
together…. There are certainly some challenges but if the common objective is 
making our agency the most preferred one, other issues get pushed back 
automatically,” he avers. A shared common vision is what makes people to stick – 
and tick. “Remember, I came in for communications,” Farei reminds one, 
highlighting his skillsets. Yet another ace he uses to defuse any crisis is to 
ask opposing groups to put themselves in the ‘other’s shoes’. “It works… It has 
worked. It will.” <br>
<br>
A car buff to the core and a Formula One fan, Farei nevertheless is a family man 
to the core: three brothers and two sisters and his wife. Currently, he is 
pouring over Disruptions, a treatise about a TBWA tool (280 pages), on 
strategising for the future. He is hopeful that this will come in handy in 
helping his clients. <br>
<br>
Having grown with the times professionally, now all that Farei wants is to 
create a difference – one that adds value and makes TBWA\Zeenah noticeable 
across the region. Personally, he wants to be able to spend more time with his 
family. We have no differences on both scores. Let your dream come true, Farei! </font></p>
