				<br>
				What sets apart Mind Map?</b><br>
				It is like the Swiss Army knife of the brain. It is a thinking 
				tool that helps you to map your thoughts. If someone is planning 
				to do business in Oman or is a CEO then the technique will help 
				them in making decisions. One can either place a flag of Oman or 
				a picture of the country and then draw individual branches for 
				its geography, history, politics, business, people, culture, 
				music and so on. This helps in getting a microscope view of 
				things and compressing almost 20 pages of information on a 
				single sheet. Human memory is a tool which is much more powerful 
				than what we think and Mind Mapping helps us to harness this.<br>
							<br>
							<b>How did Map Mapping evolve?</b><br>
				It started in my student days as I discovered that despite 
				putting in a good effort my marks were going down, so I made 
				more notes but despite this my marks fell further. It was then 
				that I realised that the problem was in the way that I was 
				noting my thoughts – this was both a shock and a discovery. As 
				Mind Map evolved, I felt that I had been driving throughout my 
				life with a dusty wind screen in front and that it was finally 
				being cleared off.<br>
							<br>
							<b>Are there companies which are putting the technique to good use?</b><br>
				There are very few well known organisations which are not using 
				Mind Maps. Microsoft, Nissan, IBM, Intel, BP, Walt Disney, 
				Kuwait Petroleum, the Mexican and Chinese governments have all 
				used Mind Maps. Boeing saved $12mn by using the technique while 
				constructing a building. Leaders in New York used Mind Maps in 
				the wake of the 9/11 crisis to arrive at faster decisions. Al 
				Gore, the former US vice president uses Mind Maps for his 
				environment awareness campaigns.<br>
