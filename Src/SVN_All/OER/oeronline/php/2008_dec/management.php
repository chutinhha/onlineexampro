				<b>The third Infiniti Power Talk delivered by Mind Mapping Guru 
				– Tony Buzan empowered Oman’s powerful business leaders with 
				effective inputs on creative thinking and leadership<br>
				</b><br>
				<img border="0" src="manage.jpg" width="225" height="267" align="left" hspace="8" vspace="6">Tony Buzan, the inventor of Mind Mapping techniques was recently 
				in Oman on an invitation from Suhail Bahwan Automobiles (SBA) to 
				present the Third Infiniti Power Talk. The SBA initiative brings 
				together business leaders for an invigorating session with a 
				management guru. Infiniti Power Talk Series provides them with 
				ideas on business success. Says T Venkataraman, vice president, 
				sales, marketing and customer satisfaction, SBA, “We call this 
				power talk as it is essentially about people who want to get 
				into power or who are powerful.”<br>
				<br>
				Buzan is a renowned brain researcher who popularised the notion 
				that memory is a learned skill. According to him, creative 
				thinking is the ability to come up with new ideas and this is 
				made possible by making a new association or connection. In 
				simple terms, “creative thinking is a product of associations.” 
				Buzan has devoted a lot of his time helping business leaders 
				grapple with business situations and come up with a range of 
				solutions for the problems that they face. His strategies on 
				working a mind map have contributed to many breakthroughs in 
				organisational and retention problems. His new Mind Map product 
				which is the downloadable iMindMap software, has paved the way 
				for businesses to come up with easier and effective methods in 
				functioning better. Mind Mapping is an efficient way of 
				utilising the most valuable human resource – one’s mind power.<br>
				<br>
				During the Power Talk Buzan explained that Mind Map is an 
				expression of radiant thinking and is therefore a natural 
				function of the human mind. It is a powerful graphic technique 
				which provides a universal key to unlocking the potential of the 
				brain. The Mind Map can be applied to every aspect of business 
				like people management, vision building, short and long-term 
				planning, change management where improved learning and clearer 
				thinking will enhance human performance. Mind Maps aid in 
				creative thinking by increasing the speed and ease of coming up 
				with creative ideas which are immensely helpful to today’s 
				business leaders in handling the rapidly changing business 
				environment.<br>
				<br>
				<b>The evolution<br>
				</b>When Buzan first stared this work, he found it difficult to 
				make people recognise their untapped power. People always read 
				countless reports about how it is possible to achieve global 
				domination, however, what has been missing up until now was a 
				guide on how to achieve this. Buzan’s fundamental goal is to 
				have every single person in the world to be mentally literate. 
				Being mentally literate means to know that their brains and 
				bodies work and how thinking and cognitive functions work. 
				Basically he wants everybody to have the operations manual for 
				his or her own brain. And everything he does is directed towards 
				that. His teachings and systems are instrumental in helping 
				structure many a business campaigns and have worked in 
				developing strategies for businesses and corporations like 
				Microsoft, IBM, Walt Disney, British Petroleum, Mexican and 
				Chinese governments, Oracle, Xerox, IBM, HSBC, Irish Management 
				Institute, Hong Kong Institute of Management and many of The 
				Fortune 1000 companies.<br>
				<br>
				During the 3rd Infiniti Power Talk the master mind presented his 
				ideas and analogies which the corporate heads present agreed 
				would surely help them understand the importance of memory’s 
				grasping power and retention. It turned out to be an inspiring 
				evening as Buzan hoisted the concept of creative thinking 
				through mind maps and the importance of improving your memory 
				power. This third session of the Infiniti Power Talk contained a 
				host of ideas, motivating statements, thought-provoking tests 
				and challenging questions. Audience participation was at a high 
				which added huge value to the evening. Participants came away 
				with his autographed best selling book – The Ultimate Book of 
				Mind Maps which contains proven strategies and techniques to 
				build an infinite route to one’s mind. The Infiniti Power Talk 
				was born from Infiniti’s brand values – its commitment to 
				innovation and the nurturing of great ideas. At the heart of the 
				Infiniti Power Talk is a belief that ideas can change the world.
