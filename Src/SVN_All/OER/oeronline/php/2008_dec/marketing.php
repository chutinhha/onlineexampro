		        <td valign="top" width="100%"> <p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
                    </font><b><font face="Verdana" size="4">Football fever in 
				the air</font></b><font face="Verdana" size="4"><b><br>
				</b></font><font face="Verdana" size="1"><b>Sports’ marketing 
				takes off in a big way as companies make a beeline to associate 
				themselves with football before the forthcoming Gulf Cup. Mayank 
				Singh reports</b><br>
				<br>
				<img border="0" src="mkt.jpg" align="left" hspace="8" vspace="6">As the countdown to the upcoming 19th Gulf Cup in the Sultanate 
				(to be held from 2 -17 January, 2009) begins, football seems to 
				have emerged as the toast of the corporate sector. Companies 
				have been vying to associate themselves with the sport in a 
				variety of ways. Q-Tel (Qatar Telecom) and its partners Nawras 
				and Wataniya Telecom have signed in as the official 
				telecommunications partner of the Gulf Cup. Says Michael 
				Fitzpatrick, marketing director, Nawras, “The sponsorship 
				package will give us branding opportunities in the stadium and 
				outside, plus a chance to use the GC19 logo.” Al Jazeera Sports 
				Channel has won the exclusive rights to broadcast the event.<br>
				<br>
				And interest in football is not just limited to the Gulf Cup. 
				Oman Mobile has entered into an agreement with the Oman Football 
				Association (OFA) to sponsor the premier soccer league for three 
				consecutive years. As a part of the deal the league title will 
				henceforth be called ‘Oman Mobile League,’ for three years. 
				Shell Oman Marketing Company on its part is sponsoring the 
				referees of the OFA. Faisal Al Hashar, Managing Director, Shell 
				Oman Marketing said on the occasion of the tie-up, “This is a 
				great opportunity for Shell Oman to contribute to helping OFA in 
				strengthening the game and grooming young Omani players so that 
				Oman can take its rightful place in the comity of nations.”<br>
				<br>
				<img border="0" src="mkt2.jpg" align="right" hspace="8" vspace="6"><b>Promoting local talent<br>
				</b>Suhail Bahwan Automobiles (SBA representing the Nissan 
				brand) has launched one of the biggest initiatives to promote 
				local football by signing a three years agreement with OFA. 
				Nissan has launched a nationwide hunt for local players as a 
				part of the deal. The effort is to identify players, preferably 
				below the age of 20. It has planned a public participation 
				programme called ‘Achieve your goals with Nissan.’ The aim of 
				this programme is to choose the best Omani football player – 
				‘The Nissan Football Star of Oman.’ OFA has a list of 36 players 
				who are 20 years and below in age and the player who gets the 
				maximum votes will be the Nissan Star of Oman. The voting for 
				the contest opened on 15 October, 2008.<br>
				<br>
				The second part of the programme is to select the ‘Nissan Dream 
				Team.’ This would again be done through public participation. 
				Says Varun Jaura, head of marketing, SBA, “This is more of a 
				partnership than a sponsorship in which we bring forth not just 
				funds but resources to develop football in Oman, build up 
				interest in people for the game, identify and promote upcoming 
				players.” Despite the popularity of the game, the development of 
				the game at the grassroots levels requires more resources and 
				mass participation. Few people would be aware of the fact that 
				Oman has 43 local football clubs, 12 A division clubs and second 
				division clubs. Nissan’s initiative is an effort to raise the 
				profile of local football in the country. <br>
				<br>
				<b>Sudden interest <br>
				</b>Tying with sporting fixtures and players is nothing new as 
				internationally companies have been using sports to further 
				their brand for years. For example, Rolex watches have had a 
				long association with Wimbledon and Golf. Nissan boasts of a 
				long tie-up with football internationally and SBA’s initiatives 
				are in line with the brand’s promise. Marketers and advertisers 
				reason that there are tangible and intangible benefits of such 
				associations. These add to the imagery of a brand and it shows 
				that a company cares not just about its business but also about 
				games which are close to its customers.<br>
				<br>
				<img border="0" src="mkt3.jpg" align="left" hspace="8" vspace="6">In Oman, football as a sport has a number of advantages over 
				other sports. One its immense popularity cannot be doubted. Says 
				Jaura, “Football is the national game of Oman and there is an 
				emotional connect with the game.” Second, the sport is more 
				organised than any other game in the Sultanate. The newfound 
				interest amongst companies for football is therefore not 
				difficult to foresee. Nawras plans to use the 19th Gulf Cup as a 
				platform to showcase its 3G services with player profiles, score 
				updates, tables etc. Adds Fitzpatrick, “If you look at the 
				profile of football followers it is young, male and Omani and so 
				it is the perfect opportunity 
				<img border="0" src="mkt1.jpg" align="right" hspace="8" vspace="6">for us to reach out to this target 
				audience.”<br>
				<br>
				<b>Social </b><b>cause</b><br>
				While companies cannot be faulted for looking at tangible 
				returns, most of these associations are not merely driven by 
				business interest but also by a desire to promote excellence in 
				sports. Says Amer al Rawas, managing director, Oman Mobile, “We 
				are keen to sponsor community events and sports as these are a 
				part of Oman Mobile’s strategy for community programmes. We do 
				not measure the immediate return of such sponsorships.” Says 
				Mohammed Al-Farsi, general manager, external affairs and 
				business development, Shell Oman Marketing. “More than the 
				benefits of sports sponsorship, it was felt recently that Oman 
				football needs the support of the corporate sector.”<br>
				<br>
				Though most companies have thrown their hats in the ring, the 
				future of such sponsorships and tie-ups will be based on the 
				response that their initiatives elicit. Says Fitzpatrick, “This 
				is the first time that we are using sports as a platform to 
				showcase our services and it is a major investment. We are going 
				to take a look at how things go and then take a call on whether 
				such associations create awareness for our brand.” Jaura adds in 
				a similar vein, “Right now it is a three year contract, if the 
				return on investment is there then we may look at a long term 
				association.” While the GCC cup may have triggered a sudden wave 
				of financial support for football, if the perceived value of 
				such tie-ups do not live up to the expectations of companies, 
				they may tug at their purse strings. One can be rest assured 
				that marketing managers during the 19th Gulf Cup will be 
				watching the audience as intently as the game on the field 
				itself. </font><p align="right" style="margin: 3px 10px"><br>
