                    </font><b><font face="Verdana" size="4">For a better world</font></b><font face="Verdana" size="1"><br>
				<b>The book is a call to action for creating an international 
				order which can to tackle global problems, writes Rajshekhar 
				Chandola<br>
				</b><br>
				<img border="0" src="browse.jpg" align="left" hspace="10" vspace="6">In this book, Jim Stark 
				articulates the crisis that humanity is facing with nuclear 
				proliferation, wars, global warming and climate change. The 
				author coins the word “omnicide” to describe the possible mass 
				extermination of humans that will come as a result of the use of 
				weapons of mass destruction or environmental collapse due to 
				overpopulation and global warming.<br>
				<br>
				<b>World government<br>
				</b>The book offers hope that humanity can rise to the challenge 
				of abolishing war, just as slavery was abolished in the 19th 
				century. Stark proposes extending the rule of law and government 
				to the global level and building a democratic world government 
				which would replace the weak and ineffective structure of the 
				United Nations with a transparent and democratic world 
				government directly elected by the people of the world. Stark 
				outlines his objective which is to replace the present system – 
				where sovereign states feel compelled to have their own army for 
				protecting their borders and national interests – with a system 
				of world government.<br>
				<br>
				The question arises: how can we bring about such a world 
				government? Stark proposes that we use the Internet to conduct a 
				referendum on democratic world government, and he has already 
				created the tool on his website
				<a href="http://www.voteworldgovernment.org">
				<font color="#000000">www.voteworldgovernment.org</font></a>, 
				managed by an organisation (Vote World Government) of which he 
				is the president. The website is already running a referendum 
				where anyone can vote in favour of or against the idea of having 
				a democratic world government.<br>
				<br>
				Stark argues forcefully and convincingly that the referendum 
				being conducted by the website would, as it gains momentum, 
				create political will through the pressure of public opinion in 
				favour of a democratic world government. He points out that it 
				is time we did at the international level what we have already 
				been doing at the national level; i.e., practice democratic 
				governance. He has also compiled over 200 quotes from world 
				leaders and famous people like Einstein supporting a democratic 
				world government.<br>
				<br>
				One cannot help but agree with John Kintree, who proclaims, 
				“Rescue Plan for Planet Earth is a triumph. It belongs in a 
				class with Thomas Paine’s ‘Common Sense.’ Just as Paine was a 
				commoner who dared to advocate independence from British rule 
				for the United States of America, Stark is a commoner who dares 
				to advocate the creation of a democratic world government 
				through a grassroots initiated global referendum.”<br>
				<br>
				And to quote Jerry Tetalman, California President of Citizens 
				for Global Solutions and co-author of One World Democracy, 
				“Rescue Plan for Planet Earth should be required reading in 
				every school and by every politician.” <br>
