                  <p align="justify" style="margin: 3px 10px">
					<b><font color="#CC3300" size="2" face="Verdana">Renaissance 
					Services supports Asscn of Early Intervention</font></b><font face="Verdana" size="1"><br>
					</font><b><font color="#CC3300" size="2" face="Verdana">
					<img border="0" src="bill4.jpg" align="right" hspace="8" vspace="6"></font></b><font face="Verdana" size="1">Renaissance 
					Services has announced plans to provide support of RO250,000 
					over three years for the Association of Early Intervention 
					for Children with Special Needs, here in Oman. This 
					financial support is specifically targeted towards a diploma 
					programme in special education for 45 Omani teachers with 
					specialist skills, and will comprise of grants from 
					Renaissance of RO80,000 in 2008; RO85,000 in 2009; and 
					RO85,000 in 2010.<br>
					<br>
					Renaissance does not normally publicise specific donations 
					made as part of its Corporate Social Responsibility 
					programme, beyond disclosure requirements as a public 
					company listed on the Muscat Securities Market. However, on 
					this occasion the donation is also being used to highlight 
					awareness of the outstanding work of the association, to 
					encourage others to support them in whatever way they can 
					for the teacher training programme, and other important 
					initiatives.<br>
					<br>
					The association serves children from 0-6 years of age, who 
					are at risk or disabled, with a comprehensive early 
					intervention program that covers social, medical, physical, 
					occupational therapies and academic services; with the 
					objective of having these children enrolled in regular 
					schools or minimize their disabilities to ensure a better 
					quality of life.<br>
					<br>
					The association comprises a day-care centre at Al – Athaibah 
					where trained teachers and assistants work with children 
					with special needs. Their objective is to create a model 
					resource center for centers in other regions to emulate. 
					These services will include – continuous training for staff 
					and volunteers, counseling families with disabled children, 
					educating, promoting and advocating the cause of bettering 
					their lives.<br>
					<br>
					The first cheque for RO80,000 was presented to the 
					association during a visit by Renaissance officials to the 
					day-care centre. A spokesman for Renaissance summed up the 
					occasion saying, “We can all learn from these wonderful 
					children who possess qualities of friendship and 
					unconditional love that are not universal. They are an 
					outstanding example to us all of how to confront adversity 
					and meet challenges with a smile. These children are a 
					credit to their parents and their families. With the 
					education and support provided by the Association of Early 
					Intervention for Children with Special Needs, we believe 
					these young children may be empowered to play the fuller and 
					more active role in society that they so richly deserve. At 
					the same time, people may be become more aware of the 
					outstanding contribution these children can and will make to 
					society as they progress through life.”</font><font size="1">&nbsp;</font></p>
