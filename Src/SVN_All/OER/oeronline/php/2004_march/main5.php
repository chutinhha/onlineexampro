                    Majesty's Cup for the Best Factories has become the ultimate 
                    symbol of recognition locally. Every year since the cup was 
                    instituted in 1991 to commemorate the year of industry, 
                    there has been stiff competition between the players in the 
                    manufacturing sector to make a bid for the cup. <br>
                    <br>
                    The evaluation criteria of the contest were upgraded last 
                    year to include more parameters that will have a positive 
                    impact on the efficiency levels of the industry. Five 
                    factories that are ranked next to the top 5 factories have 
                    been honoured with Certificates of Appreciation since 1993.
                    <br>
                    <br>
                    In 1999, in an attempt to motivate both the small and big 
                    companies alike, the contest was divided into two 
                    categories. Three Cups and three appreciation certificates 
                    are earmarked for companies whose investment exceeds RO3 
                    million whereas companies whose investment is below RO3 
                    million compete for rest of the two cups and two 
                    appreciation certificates. <br>
                    <br>
                    A significant change in last year's contest rules has 
                    brought into being a third category whereby companies that 
                    have won the cup more than five times are no longer included 
                    for the competition but are, instead, honoured with a Shield 
                    of Excellence. <br>
                    <br>
                    This year's assessment criteria too have been carefully 
                    designed to cover various aspects of the industry. As in the 
                    past, Omanisation levels have been accorded top priority 
                    followed by market and marketing effort and financial 
                    performance of the participant. Value addition and use of 
                    local raw material, integration with other industries, 
                    production efficiency, quality levels and adherence to 
                    environmental security and safety systems are the other 
                    principal parameters. <br>
                    <br>
                    Of the 23 companies which were in the fray for the awards 
                    this year, Amiantit Oman Co., Mohsin Haider Darwish (MHD) 
                    gases, chemicals and medical equipment division, National 
                    Detergent Company (NDC), Voltamp Transformers Oman and 
                    Voltamp Manufacturing Co. (VMC) have emerged the winners of 
                    His Majesty's Cup whereas Al Anwar Ceramic Tiles, Oman 
                    Textile Mills, Jotun Paints, National Heaters Industries and 
                    Omani Packaging Co. have received the Certificates of 
                    Appreciation. <br>
                    <br>
                    Petroleum Development Oman (PDO), City Centre, Al Jadeeda 
                    Stores, Oman Air, Lulu Centre and Bahwan Foods (Khalijana) 
                    and 5 government bodies received shields for promoting Omani 
                    products. Unfortunately, despite the government's efforts to 
                    aggressively promote Sohar as the industrial hub of Oman, 
                    the winners were all confined to the Muscat region. <br>
                    <br>
