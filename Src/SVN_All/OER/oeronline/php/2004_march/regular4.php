atoms surrender their electrons, diffuse through a plastic
membrane and react with atmospheric oxygen on the other
side. Prototypes already exist in Siemens laboratories for PEM
fuel cells that are particularly suitable for use in automobiles
and even as mini cells in mobile phones and laptops, but cost,
size and weight are even more critical in such applications
than in stationary use. However, PEM fuel cells have an interesting
niche market in submarines. In 2004, the German navy
will begin to operate the first submarine equipped with a
Siemens fuel cell weighing some eight tons. Here, the principal
advantage of fuel cell drives is that submarines can stay
underwater much longer than with lead batteries.
