            <p align="justify"><font face="Verdana" size="1">Our energy landscape will undergo dramatic changes in coming
years. The liberalisation of markets, combined with efforts
to reduce greenhouse gases, will lead to a decentralization of
electric power generation. And, according to the German
telecommunications company Siemens, fuel cells will play an
important role in this scenario - not least because they may
reduce the size of our mobile handsets by at least 10 times and
one day power central electricity grids.
<br>
            <br>
            &quot;If you want to know what the future of electric power
will be like, look to the past. Electrification did not begin its
triumphant advance with large, centralised power plants like
the ones that power the globe today, but with electric generators
driven by mill wheels, steam engines and combustion
engines,&quot; explains Norbert Aschenbrenner, a scientist working
for Siemens. &quot;Whether mobile or stationary, the fuel cell
will-in the not-too-distant future-change the way power is
supplied. But this will tend to be a gentle revolution because
of high initial production costs.&quot;
<br>
            <br>
            Today, however, the watchword is decentralisation. RWE,
Germany's largest electric power company, is even showing
advertisements that envisage a fuel cell bought in a supermarket.
RWE predicts that by 2015 about 10 per cent of electricity
in Germany will be generated by fuel cells. The company
plans to market compact power plants for private households
by 2010. In the US, companies like Ballard Power, Plug
Power, H-Power and Fuel Cell Technologies are crowding
into the market. Siemens Westinghouse is building a plant in
Pittsburgh, Pennsylvania that will produce fuel cells. In
Switzerland, Sulzer Hexis has embraced the concept of distributed
power generation with fuel cells. And German heating
system specialist
Vaillant has teamed
up with New Jerseybased
Plug Power to
develop a fuel cell for
power and heat generation
in everyone's
basement.
<br>
            <br>
            &quot;All the players
understand that energy
is the key to continuing
economic and,
above all, sustainable
development, especially
in the long
term,&quot; says Dr.
Thomas Hamacher at
the Max Planck
Institute for Plasma
Physics, Germany.
Energy demand is
expected to continue to increase. Realistically, up to 10 billion
people will require electricity in 2050. The required investments
should, however, be kept within manageable limits.
And what's more, &quot;The risks must be manageable and acceptable.&quot;<br>
            <br>
            Rosenbauer believes that domestic fuel cells, smaller fuel
cell power plants and renewable energy sources will supply
power in an intelligent alliance with large power plants.
These distributed power systems will feed any excess production
into the power grid-a feature that will necessitate a
sophisticated management system. Many smaller fuel cells,
interconnected to form a virtual power plant, would even
provide grid companies with the opportunity to use a selective
control system to stabilise the network and thus minimise
losses. Operating costs can also be reduced, as it will be possible
to monitor mini power plants from a central control
room and coordinate maintenance activities.
<br>
            <br>
