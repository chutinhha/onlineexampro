<p align="justify"><font face="Verdana" size="1"><b>4. Omanis are easier
to hire than fire
</b><br>
Many agree that Oman's labour laws
are good and fair to both employers
and employees. Indeed, most areas of
the labour law address the rights of
'employees' without any sort of delineation
between expats and Omanis,
confirms a lawyer from a leading law
consultancy in Oman. The creation of
more jobs for Omanis has not resulted
in a disparity of employees' rights.
Indeed, the evidence supports the belief
that employees are treated equally
whether they are Omanis or expatriates.
<br>
<br>
The Sultanate has a legitimate
interest in creating more jobs for its citizens,
and the desire to end its dependence
on foreign workers should not be
confused with unfair or unequal
employee treatment. Moreover, there
is plenty of provision in the law for
firms in the right circumstances to dismiss
staff if there are proper reasons to
do so and assuming the correct procedures
are followed.
<br>
<br>
The problem lies in the perception
that the Labour Authorities will only
ever side with the individual. If the
company follows the rules and
processes prescribed in the Labour
Law, then this will not be the case. In
the absence of trade unions in Oman,
the labour authorities become the de
facto mediator to settle disputes
between employees and employers, so
employers need to be certain they are
being fair and just when making a case
for dismissal.<br>
<br>
Employees should be aware that
non-compliance of their job duties
doesn't pay and that they could face
disciplinary action as a result.
Employers should understand that if
an Omani is sacked, the employer has
a duty to employ another Omani to
replace him or her.<br>
<br>
Over time, if responsible employers
have the free market right to hire and
fire Omanis, this will create a driver for
positive Omanisation. One constraint
of making sure every Omani who
wants to work gets a job is that some
citizens feel they have a job for life in an
environment where discipline and
reward are not related to performance.
In such a situation, productivity
inevitably suffers and the organisation's
competitiveness is threatened.
Given Oman's demographic imperatives,
it is clear that in the future all
Omanis will have to compete on merit
throughout their careers.
<br>
<br>
Seasonal and part-time employment
in the hospitality and agriculture sectors
is sometimes needed. It should not be
difficult to let people go and have them
return when there is more demand for
their services. There is a need for serious
consideration as to ways of enabling
more casual employment opportunities
for Omanis. This will benefit employers
and the economy.
<br>
<br>
<b>5. Omanis will not
undertake certain jobs</b><br>
For individuals it is attitude that ultimately
determines success in the
workplace. Although training can be
designed to deliver the specific knowledge
skills needed to create the necessary
job competence, it is very difficult
to change negative attitudes. Many
organisations realise the importance of
selecting candidates who demonstrate
a positive work ethic and service attitude.
There is evidence of it everywhere
in the private sector.
<br>
<br>
The real issue is that those jobs that
can be Omanised should be targeted
and that Omanis should be equipped to
do such jobs well. Despite the success in
the country's banking sector, it is proving
difficult to attract nationals to work
in certain professions like the construction
industry. Also, domestic staff, of
which it is estimated 30,000 exist in
Oman, is actually discounted as a sector
of the economy to Omanise.
<br>
<br>
If local households will be happier
to retain domestic workers from
abroad, this sector could be an exception
to Omanisation. The real issue is
that those jobs that can be Omanised
should be targeted and that Omanis
should be equipped to do such jobs
well. The imperative of the future is
that Omanis acquire the necessary skill
sets and employment positions to pass
on their skills to other nationals.<br>
<br>
Last year, an employer complained
to a local newspaper that the Omanisation targets were unrealistic,
and suggested very few employers
would take on a delivery driver who
was not prepared to work 14 hours
per day for six-and-a-half days each
week in a van that was not air conditioned.
Rather than recommending
that the conditions of work could be
made more amenable, he suggested
that Omanis would have to get used
to working to such requirements.
Although he also acknowledged that
some Omanis were willing to do
tedious or tiring work, the implicit
message he gave (which is surprisingly
shared by many in Oman's business
community) is that this kind of
menial role is better suited to expatriates
of a certain skill level, who would
more willingly accept such poor
working conditions. Perhaps more
managers in Oman should follow the
advice of many professional service
companies as a winning tip to management:
&quot;Don't ask anyone to do anything
you wouldn't do yourself.&quot;</font></p>

