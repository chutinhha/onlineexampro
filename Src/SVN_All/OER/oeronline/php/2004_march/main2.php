<p><font face="Verdana" size="4" color="#FF0000">The Mythological Omani</font></p>

<p align="justify"><font face="Verdana" size="1"><b>Employers, training providers and government 
officials have assessed the validity of five claims being made about Omanis in 
the workplace. Nayl D’Souza collates their views</b></font></p>

<p align="justify"><font face="Verdana" size="1"><b>1. Omanis favour
public sector jobs
</b><br>
Civil service employment is often
favoured to private sector work in
many parts of the world, not only in
Oman. However, while the public sector
in the Sultanate becomes increasingly
more efficient and the government
proceeds with its privatisation
drive, there will be fewer civil servants
required in the future. Therefore, the
country is looking towards the private
enterprises to fuel job creation.
<br>
<br>
The perks associated with public
sector jobs such as longer public holidays
and shorter working days sooner
or later should become more compatible
with the working practices of the
private sector.
<br>
<br>
There is a myth that the public sector
job is much easier and the terms and
conditions are better but actually
salaries are not that much higher. The
problem which exists in the public sector
of many Gulf economies is not one of
unemployment but under-employment
(where positions have become obsolete
yet people still occupy these posts). This
status poses some major problems such
as inefficiencies and morale issues due
to a lack of job satisfaction.<br>
<br>
<b>2. Omanis are treated
better than expats</b><br>
In law circles, employee equality is an
issue that is being discussed more
openly than ever before in the
Sultanate. An area of speculation
regarding the labour law relates to the
comparative rights and treatment of
Omani and expatriate employees in
business environments. The prevailing
myth seems to be that Omani
employees are treated differently from
expatriate employees. The basis for
this myth appears to relate to the drive
toward Omanisation and Article 11 of
the new labour law requiring employers
to employ workers to the largest
possible extent.
<br>
<br>
Many have speculated that Omanis
have 'an easier time of it' finding work,
and that those who are working are treated
in a disproportionately favourable
manner. The truth is that in terms of the
composition of representative committees
formed to protect the rights of
employees, the law as drafted makes
no differentiation between foreign and
domestic employees.
<br>
<br>
<b>3. Omanis are
more costly
</b><br>
This is true only at the unskilled and
semi-skilled levels, where minimum
wages are higher for Omanis than for
expats and additional employment
costs for expats are minimal. At the
skilled, technical and managerial levels
Omani salaries are similar to
expats. Expatriates become more
expensive at these levels due to the
need for employers to provide housing,
transport, utilities, air tickets, visas and
other costs associated with attracting
individuals or families to relocate to a
host country. Therefore Omanis are
clearly a valuable commodity.
<br>
<br>
When comparing the wages mix of Omanis working in the private sector (see
table below), one can observe that
Omanisation has focused on the lowest
levels of employment (in terms of wages).
In 2001, 81 per cent of Omanis employed
in the private sector earned less than
RO200 a month and 52.5 per cent earned
the minimum wage of RO120.
<br>
<br>
Nevertheless, some Omanis will be
costly because they are deserving of
higher rewards. Those companies that
attract, retain and nurture good Omani
employees who have the necessary
aptitude to improve themselves, will
inherit the future in this market.&quot;</font></p>

