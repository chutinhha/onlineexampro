    <p align="center" style="margin: 10">
    <font face="Verdana" style="font-size: 13pt"><b>Two cities, two events Same 
    mantra of ‘change’</b></font><p align="justify" style="margin: 10">
    <font size="1" face="Verdana">A s the year draws to a close, people start 
    looking forward to what’s in store for them in the coming year. Everyone 
    expects a change in their life — of course, for the better. Managing this 
    change in a planned and systematic fashion effectively creates new methods 
    and systems. This behaviour, whether by an individual or an organisation, 
    has been dubbed ‘Change Management’ — and has become the latest buzzword in 
    international business. Two events, one in London and the other in Dubai, 
    were held in November to commemorate this coming of age. While the Leaders 
    in London Summit 2006 was held on November 29-30 at the Central Hall 
    Westminster, the two-day Leaders in Dubai Business Forum 2006 concluded a 
    day earlier on November 29.<br>
    <br>
    Both events drew on a common theme: ‘Change Management’. The Dubai 
    mega-event focused on the ‘Challenges of Change — and the necessity for 
    innovation among top management. In London, the theme was ‘World-Class 
    Strategies for Inspirational Business Leadership’, which had 16 star 
    speakers including Gen Collin Powell, Richard Branson, Sir Bob Geldof, 
    Baroness Susan Greenfield and Rene Carayol. In fact, Gen Powell gave the 
    concluding address at the Leaders in Dubai forum — and the next day opened 
    at the Leaders in London gathering. Other prominent speakers at the Dubai 
    forum included Michael Eisner, Louis V. Gerstner Jr., Her Majesty Queen Noor, 
    C.K. Prahalad, Harry S. Dent Jr. and Edward De Bono. An innovation in Dubai 
    this year was the introduction of six “Al Rouad leaders and pioneers shaping 
    the future in the Middle East”. They were presented as speakers from the 
    region “who will help shape thinking across Corporate Arabia, as well as 
    drive debate, new thinking and business leadership.”<br>
    <br>
    So, what is ‘Change Management’? Primarily, it has three basic identifying 
    features: (1) The task of managing change; (2) An area of professional 
    practice; and (3) A body of knowledge. The changes to be managed lie within, 
    and are controlled by the individual and/or the organisation. However, some 
    of the internal changes may have been triggered by events originating 
    outside the organisation, by what is usually termed as the environment. 
    Hence, the second meaning of managing change, namely, the response to 
    changes over which the individual and/or the organisation can have little or 
    no control — for example: legislation, social and political upheaval, the 
    actions of competitors, shifting economic tides and currents, and so on. 
    This is as simple as distinguishing between a knee-jerk or reactive 
    response, and an anticipative or proactive response.<br>
    <br>
    Another model of ‘Change Management’ is the ADKAR concept — Awareness, 
    Desire, Knowledge, Ability and Reinforcement. This is a goal-oriented model 
    that allows change management teams to focus their activities on specific 
    business results. The model was initially used as a tool for determining if 
    change management activities like communications and training were having 
    the desired results during organisational change. The model has its origins 
    in aligning traditional change management activities to a given result or 
    goal. But this is a more individualistic model and not particularly suitable 
    for analysing organisational change. The goals or outcomes defined by ADKAR 
    are sequential and cumulative. An individual must obtain each element in 
    sequence in order for a change to be implemented and sustained.<br>
    <br>
    But ‘goal setting’ means two things — a clear timeline and a defined 
    objective. There are no “someday I will get this or that done.” It imposes a 
    certain amount of discipline. Goal setting and planning are synonymous. They 
    cannot be separated. To put it in terms of a financial planner: “An 
    investment plan can never be right for you, if you don’t know what your 
    specific goals are — and when you want to reach them. Even if you wish to 
    have zero involvement in your investment strategy, and turn it all over to a 
    financial planner of some sort, they can’t possibly make the right 
    investments for you if they don’t know what you are trying to achieve. And 
    how can they know if you don’t know yourself?”<br>
    <br>
    So, nothing is sacrosanct. One must look at the following aspects: What you 
    want from your life; work out the details; fix and watch timeline; and (most 
    of important of all) review plans. That, in a nutshell, is what the leaders 
    — whether in London or in Dubai — are speaking about.</font></td>
