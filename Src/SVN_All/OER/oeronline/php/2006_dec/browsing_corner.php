            more, mind you. Entrepreneurs from across the world are salivating. 
            And salivating incessantly at that! With over 1.3 billion mouths to 
            feed, China’s consumer market has the potential to be larger than 
            that of Western Europe and North America combined. <br>
            <br>
            <img border="0" src="browsing_corner.jpg" align="left" hspace="5" vspace="5" width="215" height="284">Cut 
            to the purchasing power: China’s GDP stands is at US$2.2 trillion 
            and is rising steadily. Some more shockers: China consumes 25 per 
            cent of global steel, 30 per cent of global cement and is the 
            world’s largest market for electrical appliances. To cap it, 
            contracted foreign investment in China is now pegged at US$420 
            million a day. And if you look at the way Chinese goods have flooded 
            world markets and how Chinese entrepreneurs are making hay, you can 
            only say, wow. Encore!<br>
            <br>
            But, all that glitters is not gold. As James McGregor, the 
            “insider”, journalist-turned-entrepreneur, who has spent more than a 
            decade-and-a-half in China, reveals. First as The Wall Street 
            Journal’s bureau chief, then as the chief executive of Dow Jones’ 
            China business operations, before he became a venture capital 
            investor, McGregor gives a fascinating peep into China’s inside 
            world.<br>
            <br>
            A Mandarin speaker, McGregor has served as a key adviser to both the 
            US and Chinese governments. And this is what enables him to provide 
            a fascinating peep into China like few other accounts can provide.
            <br>
            <br>
            When The Wall Street Journal sent McGregor to Beijing as bureau 
            chief in 1990, the tears of Tiananmen Massacre still streaked the 
            face of China. For millions and millions across the world, including 
            this reviewer, who raised their voices in unison against the brutal 
            crackdown, the word China very much echoed Tiananmen. For McGregor 
            too, who was in his early 30s then, it must have been difficult 
            times, one can safely presume. <br>
            <br>
            And then came the turnaround, a big one at that. The word China 
            suddenly started gathered a new meaning; it started sending a 
            different kind of echo in the sub-conscious. The Chinese dragon was 
            invading in an invasion of a different kind–one that was capturing 
            the world market, like some ancient emperor on a world-conquering 
            mission. This turnaround was startling, to say the least. 
            Multinationals giants were rushing to cash in on the “gold” that lay 
            in store. One billion customers, 1.3 billion to be more precise!<br>
            <br>
            But it has clearly not been all that rosy, and easy too, as McGregor 
            tells from his long experience. Behind the halo that surrounds the 
            China of today, there remain traces of the past. Nepotism, for 
            example, still dominates China in a big way, as the author finds out 
            in his nearly two-decade stay there. And while the average Chinese 
            will appear to be a typical social animal, the individual in him is 
            raging from the inside, all the time, with a desire to competitive; 
            in fact more than that, competitive to the point of being selfish. 
            Or, for that matter, the fact that speculative activity, like 
            gambling, runs deep in the veins of the average Chinese. <br>
            <br>
            If you ever get depressed by Chinese ill-treatment of foreigners, or 
            foreigners’ ill-treatment of Chinese, take solace in the knowledge 
            that the Chinese are treating one another even worse, says the 
            writer. So is China really all that modern? McGregor leaves a lot of 
            questions to be pondered.<br>
