<b><font face="Verdana" size="4">Information Technology Enabling Development</font></b><font face="Verdana" size="4"><br>
</font><font face="Verdana" size="1">
<b>Oman has identified IT as a primary facilitator to enhance the quality of the 
life of its citizens and grow its economy</b><br>
<br>
<img border="0" src="images/spotlight1.jpg" width="250" height="183" align="left" hspace="5">Oman is making concerted efforts to promote information technology for the 
economic and social development of the Sultanate. “More and more organisations 
are seeing the importance of implementing IT systems and the benefits accruing 
from the same. This has resulted in some big IT projects being taken up by 
institutions, such as banks, telecom sector, oil and gas sector, and education. 
The Information Technology Authority (ITA), set up to spearhead the country’s 
‘Digital Oman’ strategy, is also playing a big role in creating IT/ITES 
awareness in Oman. ITA is also ensuring that IT implementation in the government 
sector follows international standards and is fronting e-government initiatives 
for the country,” says Jayakumar Srinivasan, GM, International Information 
Technology Company LLC (IITC). <br>
<br>
Another positive trend is the opening of local offices in Oman by multinational 
IT companies, such as Microsoft, Hewlett Packard, NCR, Oracle, Siemens, Cisco, 
Huawei, Motorola, Alcatel, GBM and Ericsson. Olivier Mouries, General Manager of 
Gulf Business Machines (Oman) Co. LLC, one of the largest IT solutions and 
service providers and the sole distributor for select IBM products and services 
in Oman, says “I am delighted that a small country like Oman has identified 
technology as a key differentiator to compete with other countries. The vision 
and determination of government officials and the business community in 
promoting IT is commendable.” <br>
<br>
<b>Knowledge Oasis Muscat (KOM)</b><br>
Located near Muscat, the technology park, Knowledge Oasis Muscat (KOM), is 
encouraging technology, business development, innovation and entrepreneurship by 
offering comprehensive facilities, services, support and expertise. Over 50 
firms, dependent on Park-based businesses, are today expanding, creating new 
demand and pushing up disposable incomes. <br>
<br>
KOM is also home to a number of educational institutions, including the Middle 
East College of Technology and the Waljat Colleges of Applied Sciences. These 
institutes are collaborating with giants such as Microsoft, Cisco and Oracle to 
offer higher education programmes in areas, including software technology, 
computing and information systems, software technology, medical data processing, 
database management systems, wireless networking, electronics and 
telecommunication, multimedia and games technology, etc. IT professionals 
produced by these institutes are already making a difference to Oman’s quest to 
become a digital society. <br>
<br>
On the impact of KOM, Mohammed Al Maskari, DG, KOM says, “The success of KOM is 
having a domino effect, which has resulted in an increased number of supporting 
businesses, attracting more investment capital, greater focus on research in 
higher education, increased the pool of highly-skilled labour and management, 
and improved the overall quality of life in the Sultanate. It is through 
initiatives such as KOM that Oman is working towards building a critical ICT 
mass. The critical mass is about having anchor companies that attract domestic 
as well as international creative talent and to achieve this, we need 
management, capital, a dynamic educational base and a supportive public sector 
environment. These are all areas in which we’re making substantial progress.”
<br>
<br>
Talking about KOM, William H. Lash, US Assistant Secretary of Commerce, says 
“The KOM is one of the very best IT parks I’ve seen. It offers excellent 
infrastructure, proximity to a prominent university, support from government and 
the private sector in partnership, and solid programmes for financing and 
mentoring entrepreneurs. Many countries say they want to establish IT hubs, but 
Oman is taking the right step with KOM.”<br>
<br>
<b>Role of Private Sector</b><br>
Besides the efforts by the government and its various bodies, the private sector 
is also playing the role of a catalyst in making Oman a digital society. Most of 
the top home-grown business groups have set up their IT companies/divisions in 
strategic alliance with global giants. <br>
<br>
Companies such as IMTAC, Khimji Ramdas Computer &amp; Communication Systems (KRCCS), 
Bahwan CyberTek, Bahwan IT, MHD’s COAT Division, International Information 
Technology Company LLC (IITC), OCS Infotech, Al Madina Development &amp; Supply and 
Towell Take Solutions besides many others are providing top IT products and 
solutions to meet the varied IT needs of different industries in the Sultanate.
<br>
<br>
Broadly, Omani IT companies offer end-to-end technology solutions, including 
software development, enterprise storage, enterprise IT/networking management, 
e-security, supply chain management, ERP and CRM implementation, information 
technology enabled services (ITES), business transformation management, training 
and development of human capital in IT, multi-dimensional turnkey consultancy 
and implementation projects, etc.<br>
<br>
Today, major world class IT-based projects are executed and managed by companies 
based in Oman. In 2006, ITA signed an agreement with Microsoft to procure 
licenses to use Microsoft Desktop Products for a period of three years at a cost 
of RO 6.5 million. The agreement covers the usage of Microsoft products in 
22,500 desktops across various Omani government organisations.<br>
<br>
The Central Bank of Oman is also implementing phase-wise the new national 
payment system based on the latest technology. The new system provides a 
world-class payment infrastructure, leading to real-time settlement of payments 
between banks, commercial customers and consumers. <br>
<br>
Talking about its banking related projects, Ashok Sardiwal, COO, IMTAC, says 
“Working with Hewlett-Packard, Imtac has successfully implemented the National 
Payment System projects for the Central Bank of Oman. Track record of successful 
projects deployment is being replicated in commercial banks in Oman, UAE, Yemen 
and Qatar.”<br>
<br>
<b>Drivers of IT</b><br>
“IT industry literally feeds on general buoyancy of any economy. There is a 
direct link between growth in IT industry and other key economic indicators even 
in developed countries like the United States. We believe that IT companies in 
Oman are in for a good time for three reasons primarily: First, the rapid 
economic development of the country. Second, the fact that IT spend in Oman has 
been less even by regional standards and hence, there is some catching up to do. 
And third, the formation of the ITA under the guidance of the Ministry of 
National Economy has given a much needed impetus to Oman’s stated ambitions of 
creating a digital society. Their work towards creating a viable and robust 
framework for information flow through all levels of Omani society shall be a 
major catalyst for local IT companies to post handsome growth in the coming 
years,” says Jayanta Borpujari, Divisional Manager, KRCCS<br>
<br>
“One reason why we’re getting results is because in regional terms, Oman is 
perceived as affordable, particularly in terms of housing, schooling, cost of 
living and other business costs. It is clear from our experience on KOM that 
Oman is attracting younger families, well-educated and upwardly mobile people. 
Perhaps, the most predictable bottomline in this current economic expansion is, 
well, the bottomline. Places kindest to business costs, whether in terms of 
office rents, taxes, or regulatory environments, are doing best. When people 
depend on debt to finance operations, they look at things differently than when 
it is equity,” adds Mohammed Al Maskari. <br>
<br>
Oman is geographically very close to India, the country that is experiencing a 
remarkable growth in the IT sector. Given the close economic ties between the 
two nations, Omani companies can take advantage of the burgeoning IT sector in 
India by partnering with Indian companies in developing and providing IT 
products and services for the Indian market. <br>
<br>
Compared to other Arab countries, Oman offers the advantages of political 
stability, WTO and US FTA compliances, lower land prices, adequate 
infrastructure, young population and a talented labour pool. With such 
advantages, the Sultanate has the potential to become a major IT destination in 
the region. For example, Gulf Air is running its global call centre operations 
from KOM. If more companies are encouraged to follow the example of Gulf Air, 
tomorrow Oman can emerge as the information technology enabled services (ITES) 
or back office operations hub of the Gulf region. <br>
<br>
Manmohan Singh Rehsi, GM, MHD’s COAT Division, puts forth another interesting 
aspect on the driver of IT in Oman: “Enterprises will clean their houses. 
Several trends are converging to compel businesses to shed unnecessary and 
redundant systems. Omani companies will increasingly use integration 
technologies such as web services and service-oriented architecture.”</font></p>
