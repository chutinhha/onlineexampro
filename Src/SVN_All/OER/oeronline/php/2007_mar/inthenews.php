<b>The three-day agriculture symposium at Sumail unfolds a comprehensive strategy 
plan on agro-land development, with the focus on research and water resources 
development, reports Shalini Ravi<br>
</b>
<br>
<img border="0" src="inthenews.jpg" width="300" height="200" align="left" hspace="6" vspace="4">Desertification, grazing, fertilisers, irrigation – all this and more were the 
highlights of the three-day symposium on ‘Sustainable Agriculture Development 
And Regulation Of The Labour Market’ at the royal camp in Seih Al Rasiyat in the 
wilayat of Sumail. The conference was envisaged with the aim of delving deeper 
into the plethora of opportunities offered by the agricultural sector. 
Recognising the potential offered by this sector and under the aegis of His 
Majesty Sultan Qaboos Bin Said, a comprehensive strategy plan unfolded at Sumail 
to ensure that the sector played a vital role in the Sultanate’s economic 
development.<br>
<br>
According to HE Ahmed bin Abdulnabi Macki, Minister of National Economy and 
Deputy Chairman of Financial Affairs and Energy Resources, the success of the 
seminar was an outcome of the special attention of His Majesty Sultan Qaboos. He 
said, “The seminar is part of the constant nterest and concern of His Majesty to 
improve the performance of agricultural sector, develop its contribution to the 
Omani national economy and sustainable development plans.<br>
<br>
The valuable tips given by His Majesty during his session with Wusta sheikhs and 
dignitaries cast light on a number of impediments and hot agricultural issues. 
They also gave us good idea on how we can meet the challenges, reach sustainable 
agricultural development, rationalise the use of chemicals and insecticides that 
harm the soil quality and better manage our water resources.”<br>
<br>
The ideas flowed in from all directions as the subject of water prevailed as one 
of the moot points at the symposium. The conference addressed the issue of 
scarcity of water resources due to poor rain, heat and increasing demand for 
water stemming from population growth. On the second day of the seminar, four 
working papers were presented. The first paper dealt with corporate management 
of water resources in the agro sector and the role of modern irrigation 
technologies in monitoring water consumption and reducing the cost of agro 
production. <br>
<br>
As HE Sheikh Abdullah Bin Salim Al Rowas, Minister of Regional Municipalities, 
Environment and Water Resources said, “Water is one of our valuable resources. 
Therefore, we should manage it very well to achieve the pre-set goals of the 
government. The work paper on the comprehensive management of water resource in 
agricultural sector highlights the modern irrigation technologies that help us 
in rationalizing use of water and reducing the production cost. Identifying the 
challenges and impediments will also help us to reach a number of valuable 
recommendations that encourage the use of modern technologies and better 
utilisation of water resources.”<br>
<br>
<img border="0" src="inthenews1.jpg" width="300" height="200" align="right" hspace="6" vspace="4">Another issue dealt with at length was agro-land ownership, farm size and its 
impact on production efficiency and employment of manpower. Besides, there was 
special emphasis on agro statistics as an important constituent to facilitate 
sustainable agro development. It was opined that agro statistics formed a 
scientific base for all agriculture-related units. <br>
<br>
Underlining the importance of agriculture, HE Sayyid Ali Bin Hamoud Al Busaidi, 
Minister of the Royal Court said, “Agriculture plays an important role in the 
Omani national economy especially it is the main source of income for a 
considerable number of families. The contribution of the sector could be far 
much better if we ensured better utilisation of our water resources. The seminar 
will focus on a number of important issues that will undoubtedly help us make 
better use of our natural resources with special emphasis on agricultural 
sector.”<br>
<br>
Research was another topic that hogged the limelight at the symposium. The 
importance of research was discussed as an important tool in agro-development. 
Research on conservation and maintenance of soil and water, use of treated water 
and using medium/high salinity water for producing fodder was stressed. <br>
<br>
Many key recommendations emerged towards the conclusion of the seminar, which 
are summed up below: <br>
<br>
<b>Trickle-Down Effect</b><br>
The symposium reiterated the need to continue supporting and encouraging farmers 
to introduce modern irrigation systems and protect water resources from 
pollution and salinity. A national strategy was put into place to tackle 
salinity and restrict plantation of grass in various regions. Recommendations 
were put into place acknowledging the significance of corporate management of 
water resources, regularising its demand and improving its usage. Besides, the 
recommendations also included a check on over grazing, tackling desertification 
and developing natural pastures.<br>
<br>
<b>Local Flavour</b><br>
The symposium recommended implementation of policies in the agricultural sector 
to encourage owners of small farms to engage in farm mergers with a view to 
increasing job opportunities for Omanis. It was also recommended to set up a 
vocational training centre, encourage formation of farmers’ societies in 
regions, offer technical support to them and encourage partnership companies for 
leasing land in order to modernise agriculture. <br>
<br>
<b>What’s The Good Word<br>
</b>The seminar recommended pruning the marketing system and setting up an effective 
marketing mechanism to expand agricultural activity. The contribution of 
commercial banks in financing the sector was debated, and it was suggested that 
these banks support new projects. A study on the competitiveness of products to 
meet the needs of the market and formulating strategies to increase exports was 
also emphasised. <br>
<br>
<b>Info Watch<br>
</b>The recommendations also elaborated on enhancing coordination among authorities 
and research institutes such as Sultan Qaboos University, Scientific Research 
Council and regional and international organisations. Another suggestion that 
came up was to familiarise farmers and private agricultural institutions with 
the new methodologies of production, thereby making processes more 
technology-driven. The paper also recommended the implementation of statistical 
surveillance and studies during the Seventh Five Year Plan’s remaining periods, 
adoption of modern technologies and methods in carrying out agro surveys and 
providing credible agro-statistics. <br>
<br>
It was a truly memorable gathering with invaluable inputs on agro development in 
the Sultanate. This conference will lend an impetus to progress in the 
agricultural sector and give better insights into the problems faced by the 
sector. The working papers were, undoubtedly, of great value throwing new light 
on the issues that need immediate attention. A noteworthy aspect was the 
proposal to give women and youth a major role. The future is full of promise for 
the agricultural sector. 
