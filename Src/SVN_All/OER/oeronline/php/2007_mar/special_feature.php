</font><b><font face="Verdana" size="4">PDO – Powering the Nation</font></b><font face="Verdana" size="1"><br>
<b>2007 marks the completion of 40 glorious years of PDO’s contribution in 
developing human resources, forging sustainable partnerships and supporting the 
progress of the Sultanate</b><br>
<br>
PDO stands tall in crisis<br>
OER takes a look at how Petroleum Development Oman withstood Gonu cyclone 
successfully under high quality Omani leadership and safeguarded the interests 
of the Sultanate’s economy and society<br>
<br>
In the first week of June, Petroleum Development Oman (PDO) faced one of the 
biggest emergencies in its 40-year history as Gonu cyclone hit Oman. The company 
successfully met the challenge from the nature’s fury by chalking out in advance 
a detailed crisis management <br>
plan that was ably executed by the team led by Suleiman al Tobi, who happened to 
be <br>
the director on emergency duty at the time.<br>
On receiving the warning from government authorities, PDO swung into action as 
the managing Director, John Malcolm, and Deputy Managing <br>
Director, Abdullah al Lamki, besides the senior directors and managers, had an 
emergency meeting. The foremost priority for PDO was to protect the human lives 
at Mina al Fahal (MAF) and its interior operations. It established lines of 
communications with the Met office and Royal Oman Police (ROP) and its emergency 
team monitored the developments closely.<br>
As the storm approached, PDO took important measures such as barricading the 
computer building, backing-up IT systems and evacuating offices. In order to 
avoid environmental catastrophe, the terminal was shut down for two-and-half 
days. But the production continued which means its average monthly production 
will remain unaffected. “PDOs local and corporate emergency teams in the coast 
were relocated to a new location at MAF camp as a precautionary measure since 
the Corporate Emergency Control Centre room in the MAF computer building is 
close to sea level. We tried to load as much crude as possible during the two 
days before the storm in order to get the tank levels lower. When the storm was 
hitting MAF, we were ready to operate the pipeline and tanks manually in case 
things went wrong and electronic systems failed. But fortunately these remained 
contingency measures that we didn’t need to take,” said Suleiman al Tobi, who 
was at the fulcrum of the emergency operations. <br>
PDO made sure that tankers at MAF moved out to the high seas as a precautionary 
measure. Immediately after the cyclone, the crisis management team set up a 
number of recovery and restoration teams with leaders to handle various aspects 
of the aftermath. <br>
“Our departmental interfaces – given that we have never had an incident of such 
magnitude and didn’t have procedures to manage it – worked very well. Everyone’s 
personal commitment to help not only the business but also society at large was 
remarkable,” al Tobi added.<br>
PDO didn’t report any serious injury or fatalities due to the cyclone amongst 
its staff and contractor community. “PDO suffered only minor weather-related 
damages such as blown-off roofing and toppled trees etc. They were almost 
limited to the headquarter offices at MAF,” informed Abdul-Amir al Ajmi, 
external affairs &amp; communications manager of PDO. <br>
Praising PDO team’s remarkable proactive approach, John Malcolm said: “Regular 
emergency exercises, the high quality Omani leaders in charge of the emergency 
response, the sound communications with the ROP and other government bodies and 
last but not least a totally committed work force, have all contributed to our 
successful management of this crisis.”<br>
The incident has really tested PDO’s emergency response capability. What are the 
learning points for PDO from Gonu experience? Johnson B. Awe, PDO’s emergency 
management coordinator replied, “PDO’s emergency response management system has 
by and large been designed to deal with localised emergencies, such as a well 
blow-out, a fire at a production facility or an oil spill at the MAF terminal. 
Natural disasters such as those caused by cyclones, which affect large areas of 
the country, have shown us that some supplemental procedures need to be in 
place, particularly with regard to communicating with staff and contractors 
outside of work.”<br>
He further added, “PDO has to be better prepared for widespread emergencies, 
particularly those due to natural events, such as earthquakes, cyclones, 
tsunamis and so on. The risks have to better understood, and measures have to be 
taken to ameliorate them. Once we have the risk-mitigating measures and 
fail-safe communications systems in place, we will have to make sure that 
everyone in the national emergency response team knows his responsibility.” <br>
