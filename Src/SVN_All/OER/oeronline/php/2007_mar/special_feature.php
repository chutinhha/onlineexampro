<br>
Contribution to society<br>
More than 400 PDO families were seriously affected by the flooding, in the 
process losing their cars and home belongings. As a responsible corporate 
citizen, PDO mobilised its resources immediately in support not only of its 
staff but the nation as a whole. Abdul-Amir al Ajmi, external affairs &amp; 
communications manager of PDO informed, “In quick time, PDO provided support to 
Oman Gas Company in recovery work of their gas line and to the ministry of 
electricity &amp; water resources with generators for Barka power station. We also 
sent a power generator to Al Amerat health centre.”<br>
As areas around Muscat struggled without water, the Company supplied water 
trucks to areas around Ghubra and Azaiba in the days following the cyclone 
giving relief to staff and members of the wider public. At the PDO Recreation 
Centre, over 2000 cases bottled water were distributed by PDORC to staff and the 
public. A tanker also distributed water. In addition, PDO worked hand in hand 
with the Oman Charitable Organisation (OCO). On June 12, PDO supported OCO by 
taking emergency supplies on two 40-tonne trucks from Sultan Qaboos Port to 
affected areas around Al Amerat, accompanied by a convoy of 15 four-wheel drive 
vehicles of volunteer PDO staff laden with supplies collected by PDO staff. On 
June 13 and 14, the exercise was repeated to villages around Qurayat.<br>
Al Ajmi explains: “The aim has been to deliver aid directly to areas that have 
not received any or minimal aid. PDO has also set up a fund for victims and 
offered to contribute an equivalent amount given by the staff. In addition, PDO 
employees have actively participated in voluntary clean-up operations organised 
by the Muscat Municipality. PDO Spouses Association and former PDO employees 
have also pitched in with their contribution in cash or kind.”<br>
