</font><font face="Verdana" size="4">
<b>Steady Progress </b></font><font face="Verdana" size="1"><br>
<b>
Ziad bin Karim Al Haremi, CEO, Oman Air, on the airline’s progress and 
challenges in a talk with Akshay Bhatnagar</b><br>
<br>
<b>
<img border="0" src="spotlight15.jpg" width="175" height="195" align="left" hspace="5" vspace="5">What is the strategic importance of Oman on the regional and global aviation 
map?<br>
</b>Oman has assumed significant strategic importance on the regional and global 
aviation map. Geographically, Oman is located very strategically, being the 
entry point from the Indian sub-continent into the Gulf. With varied natural 
spots and the government focus on tourism and infrastructure development, it is 
fast becoming a favoured destination for tourists within as well as outside the 
region. With Dubai as its neighbour, Oman is able to share and benefit from its 
stupendous growth in aviation sector in particular and economic development in 
general.<br>
<br>
<b>How was the passenger and cargo aviation traffic in Oman in 2006 vis-a-vis 
2005? <br>
</b>In the passenger traffic segment, incoming traffic increased by 34.4 per cent, 
outgoing up by 32.5 per cent, transfer grew by 52.5 per cent while transit 
decreased by 34.1 per cent. In cargo traffic, incoming increased by 26.3 per 
cent while outgoing increased by 35.3 per cent.<br>
<br>
<b>What has been the share of Oman Air in the passenger traffic?</b><br>
In 2006, the airline carried 1,226,539 passengers compared to 1,135,448 
passengers in 2005. <br>
<br>
<b>How was the Oman Air’s performance in cargo traffic?<br>
</b>There was no growth for Oman Air in 2006 vis-à-vis 2005 due to the following 
reasons: <br>
Oman Air couldn’t carry much cargo to Indian subcontinent due to scarcity of 
space. In all Indian sectors, our space was occupied by passenger baggage, which 
is a high priority area. In the return leg from Indian sectors, space was 
available but giant operators were offering much lower rates and snatching the 
load available. They OAL (other airline) were offering incentives to a level of 
25-40 per cent other than the 5 per cent IATA-set commission and absorbing most 
of the expenses supposed to be borne by agents and shippers. This makes it very 
difficult for carriers operating with small capacity. <br>
<br>
The regional giant carriers, who are operating flights with ULDs (Unit Load 
Devices), need to fill their large bellies in order to maintain the load factor 
(cargo) from their main hub. For this purpose, they need to bring maximum load 
from the market to their main hub from where the cargo is available and that is 
India and China. <br>
<br>
The major airlines have already started operating freighter flights from Indian 
airports and thus supply (space) has overshot the demand (cargo). This has 
resulted in agents dominating the market and they are demanding much more lower 
rates. <br>
<br>
Since Oman Air’s major operations are to/from India and the competitive acts of 
OAL in the region were affecting our sales in Indian stations where Oman air is 
operating. The high cost of security measures at Indian airports and minimum 
handling charge based on a higher weight break at airports in the region is also 
another stumbling block to carriers like Oman Air, which operates with narrow 
body aircraft. <br>
<br>
The cargo management has taken all these factors into consideration and looks 
forward to a brighter 2007, determined to face such blockades/ challenges with 
effective plans and action.<br>
<br>
<b>What steps have been taken to improve operational efficiency, specially on 
issues such as fuel costs and operational synergy with Gulf Air?<br>
</b>Oman Air has kept its focus on being a niche regional carrier that offers new 
value propositions to its customers. We successfully embarked upon e-ticketing 
and internet booking in 2006. Our new customer loyalty programme, ‘Sindbad’, is 
fast gaining popularity among our frequent flying passengers. <br>
<br>
Oman Air has installed winglets on aircraft to reduce fuel consumption. In 
addition, Oman Air recently invited IATA to carry out a full-fledged study in 
this area. Several measures have been identified to optimise fuel consumption. 
Operational synergy with Gulf Air has been achieved wherever possible and 
practical through code share arrangements to the mutual benefit of both 
airlines.<br>
<br>
<b>What are the other challenges Oman Air faces in 2007? What has been the 
impact of Gulf Air’s inclusion of Muscat as the second hub and entry of new 
players on Oman Air’s business?<br>
</b>The aviation industry is recording a sturdy growth in contrast with other 
industries. Therefore, Oman Air’s strategies and plans should synchronise with 
this. The Middle East is considered as prime importance to the world transport 
market, which is witnessing progressive developments, and Oman Air is 
considering all possibilities for future expansion. Although there has been talk 
of a move to the Far East, future destinations are by no means limited to this 
region<br>
<br>
More aircraft will be brought into service in the next five years, as we are 
currently re-working our strategy plans in view of the recent move of Gulf Air 
to Muscat. Gulf Air has a two-hub strategy–Bahrain and Muscat–and it recently 
announced a massive 69-flight increase from the Omani capital. However, as the 
airline code shares extensively with Oman Air, the situation is not strictly one 
of direct competition and business synergies may well prove beneficial to both 
carriers.</font></p>
