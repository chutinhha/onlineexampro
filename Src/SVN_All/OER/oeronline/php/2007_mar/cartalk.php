<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
</font><b><font face="Verdana" size="4">Taking on the Challenge</font></b><font face="Verdana" size="1"><br>
<b>
Volvo enters the luxury car segment arena with the all-new S80. Anne Kurian 
checks if the S80 can stand up to expectations<br>
</b><br>
<b>
<img border="0" src="car1.jpg" width="300" height="236" align="left" hspace="5" vspace="5"></b>Survival of the fittest as we have seen for ages is not the prerogative of just 
living beings but of everything in the universe, manmade or otherwise. It could 
be nations, buildings, art…why even, cars. By cars we mean brands, which brings 
us to the brand in discussion this month – Volvo.<br>
<br>
The Swedish brand is known for creating classy, comfortable and safe-to-drive 
cars. Volvo is slotted in the luxury car segment too but the limelight here has 
always been hogged by German and other European manufacturers. Volvo has never 
taken a backseat and has gone ahead to produce attention grabbers like the XC90 
and the S40. Through innovations and extra attention given to safety, Volvo has 
proved that it is a survivor. Now Volvo is back in the premium midsize segment 
race with the all-new S80.<br>
<br>
The Volvo S80 was launched in 2000, and has received a facelift after seven 
years of creation. At first glance there’s not much of a difference but when you 
go up close and nearer to the all-new Volvo S80, the modifications do show up. 
Volvo has brought in three variants of the S80 – 2.5 litre, V6 3.2 litre and V8 
AWD. The all-new Volvo S80 is being pitted against other brands of luxury cars. 
But will it survive the challenge? We decided to put the S80 to test. <br>
<br>
<b>Style factor<br>
</b>On closer inspection, you realise that the new version is a few inches 
longer with a slightly sleeker body. Volvo has not departed from the 
conventional and made any radical changes as far as styling is concerned. Not 
exactly a surprising factor taking into consideration that the S80 is not 
exactly a sporty sedan or a youth car. The target clientele has all along been 
high-flying professionals and executives, and the all-new Volvo S80 continues to 
target the same clique. <br>
<br>
Inside the new S80, it’s a choice of three types of upholstery that is on offer 
– textile, leather and perforated leather. Volvo has steered clear of cluttering 
the centre console panel and has left it looking rather aesthetically pleasant 
with control knobs placed in the least intimidating manner.<br>
<br>
The new S80 has controls mounted steering wheel, as is the norm in almost all 
premium brands now. So changing music, managing the volume and cruise control is 
literally at your fingertips. The new S80’s memory function allows you to set 
three different seat and door mirror settings. Takes away the hassle of having 
to push and pull your seat or adjust the door mirrors after your spouse has 
taken the S80 for a spin! <br>
<br>
Technologically, there are more modifications in the S80. Right from making it 
Bluetooth compatible to the option of having an integrated GSM phone in the car 
to Road Traffic Information (RTI) system to the information menu device, Volvo 
has ensured that its premium brand does not lag behind in the tech-advanced auto 
world. <br>
<br>
<b>Driving impressions<br>
</b>There are just two words to describe the S80’s performance - smooth 
operator. Be it on the rush hour roads or the bylanes of Muscat, the S80 
provides excellent driving comfort. Though we got to test drive only the V6 3.2 
litre, the S80 put up quite an impressive performance. When compared to 
competitors in the same category, the V6 engine of S80 produces only 238 
horsepower against the usual 260 and above horsepower. But then again the Volvo 
is not about racing and beating time; it’s all about driving pleasure and being 
in control. <br>
<br>
The V8 comes with All-Wheel Drive (AWD) option, which maximises performance and 
stability. The S80 has been equipped with Continuously Controlled Chassis 
Concept (4C), which lets you choose from three driving modes – Comfort, Speed 
and Advanced. Sure, we did say that Volvo is not always about racing but then we 
also told you that the brand takes driving comfort seriously. <br>
<br>
While this might not seem like a unique feature, as far as the S80 is concerned 
the Personal Car Communicator (PCC) is an innovation for the brand. The PCC is 
rather the key of the S80, which enables keyless entry and drive (optional). <br>
<br>
<b>Safety matters</b><br>
Volvo is renowned for the importance it lays on safety. For starters, the S80 is 
the first Volvo to be equipped with adaptive cruise control with collision 
warning and brake support. In regular parlance, this means that when the 
distance between the Volvo and another car is within collision distance a 
warning light comes on. Simultaneously, the braking system is geared up to take 
on the situation. The new Volvo also comes with patented frontal structure that 
protects the passengers in the event of frontal collision, side impact 
protection system and whiplash protection system, among other features.<br>
<br>
Volvo knows the S80 is definitely going to be come under much scrutiny when 
pitted against other luxury brands. But then the all-new S80 is equipped to put 
up a brave fight and deliver what it promises thanks to the innovations 
introduced. And the clincher of them all? The Volvo S80’s irresistible pricing 
which starts at RO13,000. Didn’t we tell you that Volvo is a sure-fire survivor?
