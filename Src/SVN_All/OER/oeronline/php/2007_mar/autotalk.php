		<p align="justify"><font face="Verdana" style="font-size: 14pt"><br>
        </font><b><font face="Verdana" size="4">Redesigning Luxury<br>
		</font><font size="1" face="Verdana">In the LS460, Lexus has managed to 
		combine cutting-edge technology with superior craftsmanship and luxury 
		features to bring out a sedan that stands out from the rest</font></b></p>
		<p align="justify"><font size="1" face="Verdana">
		<img border="0" src="autotalk1.jpg" width="250" height="172" align="left" hspace="6" vspace="4">In 
		the pursuit of perfection, Lexus has gone ahead and rewritten history 
		with the new flagship model 2007 LS460. Built on the principles of 
		Lexus’ famed L-finesse design, which aims to produce cars with tangible 
		benefits that elevate the pleasure of driving, the LS460 is a revelation 
		where luxury and automotive engineering are concerned. <br>
		<br>
		Lexus being a fairly new entrant in the luxury cars segment has often 
		had to prove itself with stark comparisons being drawn to its German 
		competitors. This has only spurred the Japanese automotive manufacturer 
		to set new heights and scale them. The LS460 is one of the best examples 
		of Lexus’ quest for perfection. <br>
		<br>
		Entering a new arena, Lexus has created its first long-wheelbase version 
		with the LS460 L. Popular demand and the fact that there were no 
		long-wheelbase versions in Lexus gave birth to the LS460 L. A smart move 
		you could say, for not only are long-wheelbase sedans trendy among the 
		well heeled but will also be a power boost for Lexus.<br>
		<br>
		L<b>atest Technology</b><br>
		When you are among distinguished names, the need to excel and break 
		barriers is inevitable. Loaded with ‘world’s first’ features, the LS460 
		seems to be in a bid to turn the tables around in the competitive auto 
		world. Lexus has introduced the world’s first VVT-iE engine in the 
		LS460, which translates to high power output – 342hp/6,4000rpm. The 
		automotive maker guarantees an acceleration speed from 0-100km/h at 6.3 
		seconds. <br>
		<br>
		<img border="0" src="autotalk2.jpg" align="right" hspace="6" vspace="4">Much before competition could feature this innovation, Lexus has 
		installed the world’s first 8-speed automatic transmission in the LS460 
		making it a leader of sorts in the luxury segment. Lexus also retains 
		the sequential shift mode, which is present in its other models, to 
		provide the pleasure of driving on manual mode. Setting a standard of 
		speed and performance in the automotive world, the LS460 is clearly a 
		technological breakthrough. <br>
		<br>
		The powerful engine and 8-speed automatic transmission spells speed and 
		performance but then ultimately comfort matters too. After coming this 
		far, Lexus couldn’t take a chance where performance and comfort is 
		concerned–which brings us to its newly developed independent multi-link 
		suspensions. Combined with stabiliser bars, these are installed in the 
		front and rear ensuring a smooth, effortless ride even at maximum speed.
		<br>
		<br>
		In the LS460, the team at Lexus has managed to combine cutting-edge 
		technology with superior craftsmanship and luxury comfort features to 
		bring out a sedan that stands out from the rest. And it does establish 
		that Lexus is serious about its customers and the brand value.<br>
		<br>
		<img border="0" src="autotalk3.jpg" align="left" hspace="6" vspace="4">A 
		look at some of its distinctive features should drive home the point: 
		16-way adjustable power front seats, driver’s seat 3-position memory 
		function, liquid crystal central touch panel, DVD Voice Navigation 
		system, automatic climate control, 10 ultrasonic sensors for parking, 
		world’s slimmest Smart Card Access system, Mark Levinson Surround Sound 
		System with 19 speakers… Comfort for the driver as well as the passenger 
		is undoubtedly a priority for Lexus. And these are just a few of the 
		characteristic innovations in the LS460. <br>
		<br>
		<b>Relax, and be driven</b><br>
		The long wheelbase version, LS460 L would ideally be the car you would 
		want to be driven in as luxury takes on new denotations. The LS460 L 
		incorporates two never-seen before features. The Ottoman Seat, which can 
		be installed in the luxury sedan, features the world’s first ‘Rear Seat 
		Relaxation System’, a built-in massage mechanism for ultimate riding 
		comfort. Now this is what you call luxury travel. <br>
		<br>
		With so many innovations, Lexus does not in any way lag where safety of 
		its occupants are concerned. In fact, the LS460 L is fitted with the 
		world’s first rear seat cushion airbags. Yet another milestone for 
		Lexus! Apart from safety features like electronically controlled 
		braking, anti-locking braking system, brake assist, electronic brake 
		distribution and brake hold, the LS460 also has tire pressure warning 
		system and auto tilt down on reverse rear view mirror function. <br>
		<br>
		Packing in features that cater to the occupants’ riding, comfort, luxury 
		and safety needs, the LS460 proves that Lexus has come of age. And if 
		you feel like basking in the opulence of the Lexus technology and 
		comfort, then get ready to write out a cheque for RO34,000. Luxury does 
		have a price, after all!</font></p></td>
