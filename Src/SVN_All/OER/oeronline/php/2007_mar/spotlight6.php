<p align="justify" dir="ltr"><font face="Verdana" size="1">Mail Server – Linux can be configured as a centralised email server, using a 
single line and a modem for all users in an organisation. Users can also have 
email communication within the organisation.<br>
<br>
Proxy Server – Linux can be configured as a strong proxy server, with a cache of 
recently visited websites. Logs can be generated of proxy server to check the 
internet usage among employees. User restriction is also possible and certain 
departments or set of employees can be allowed to use emails but not to browse 
the internet. <br>
<br>
Firewall – Linux has a very powerful built-in firewall known as IP-Chains. This 
protects the server as well as the network from any illegal activity by hackers 
or other worm threats, such as MS Blaster and Sasser. <br>
<br>
Instant Messaging Server –Linux can be configured as a local, instant messaging 
server. Users can have instant messaging within the organisation while at the 
same time outsiders can be barred from communicating with the users. Logs of a 
chat session can also be saved. <br>
Fax Server – This solution is suitable for companies where many fax machines are 
used simultaneously. It saves time and effort, and maximises productivity. It 
allows everybody to fax documents from their PCs. Logs of outgoing transmissions 
can also be easily generated.</font></p>
