<img border="0" src="spotlight5.jpg" width="150" height="185" align="right" hspace="5" vspace="5"><b>Linux, created by Linus Torvalds, has taken the global IT market by storm. In 
Oman, Innovations Computers &amp; Telecom is offering Linux-based business solutions 
to help companies to meet challenges on the IT front</b><br>
<br>
Linux has emerged as one of the world’s most powerful operating systems. Besides 
contributing to the growth of internet, it has provided a cost-effective server 
platform, powering the mail server, web server and databases across industries.<br>
<br>
In Oman, Innovations Computers &amp; Telecom LLC offers a wide range of consulting 
services on the Linux platform. According to Al-Qasim Al-Khalili, General 
Manager, Innovations Computers &amp; Telecom, “Just imagine buying an operating 
system, email server, proxy server, firewall and instant messaging server 
separately from the world’s most popular software company. It costs a lot of 
money plus headache for licenses and upgradation. But all these softwares come 
free with the Linux server. One only needs to configure and pay for the 
services. There is no restriction of users’ licenses as the Linux Operating 
System is free. The hardware requirement is also less –a Pentium machine with 
128 or 256 MB RAM and hard disk of 20 GB is enough.”</font></p>
