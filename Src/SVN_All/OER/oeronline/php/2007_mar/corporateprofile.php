<p align="justify" style="margin-left: 10px; margin-right: 10px">
<font face="Verdana" size="1">
<b>Consolidation Phase</b><br>
With an aim to consolidate its market presence, Al Madina real estate is 
initially planning to concentrate on projects in Muscat only. As Barham, an M.Sc. 
in mechanical engineering and double BSc in architectural and civil engineering, 
says, “Currently, we’re consolidating in Oman only. When we reach the stage 
where we have established ourselves in Muscat,, we can then think of expanding 
to other cities as well as internationally. In Sohar, we’ve identified two 
locations where we are doing feasibility studies.”<br>
<br>
When asked to comment on what is driving his expansion plans, he quipped, “Real 
estate is a facilitating sector and not a leading one. Once the economy is on a 
growth curve, the real estate market too has to move up. More requirements for 
offices, commercial and residential spaces are a natural corollary of any 
soaring economy. For the past few years, we are witnessing such growth as oil 
prices have gone up and the Omani economy has had a healthy growth that has 
fuelled the real estate sector’s growth. We are seeing real estate prices and 
rentals moving up in most parts of Oman, and the demand-supply cycle has yet to 
balance out. The demand is in all major cities and regions. However, there’s no 
corresponding supply to meet the demand.”<br>
<br>
Commenting on the supply bottlenecks in the real estate market, he says, “In the 
last decade, the country has gone through a recession stage which adversely 
affected the construction industry. A number of big contractors had too reshape 
themselves to survive. However, the recent growth has taken the construction 
industry by surprise. Now, they are being required to recoup again, but are not 
yet fully ready. That is why the supply today is falling short of demand.”<br>
<br>
Though Oman has taken the lead among GCC countries in formulating and 
implementing the law on freehold land in designated areas, Barham feels there’re 
still some chinks in the real estate laws that need fine tuning. “There should 
be more flexibility on the corporate ownership front, where the law provides 
some limitations on what Omani companies can own, lease, buy and sell, though 
there’re no such limitations on Omani individuals. Companies should be given 
more flexibility in buying and selling of properties as enjoyed by individuals.”<br>
<br>
At the end of the day, Al Madina real estate is upbeat on market prospects. And 
with the fundamentals remaining strong, the company is sure to build a solid 
future, benefiting both the shareholders and the economy in the days to come.<br>
