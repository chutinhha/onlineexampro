<p align="justify" style="margin-left: 10px; margin-right: 10px"><font face="Verdana" size="1">
<br>
</font><b><font face="Verdana" size="4">Adding Real Value</font><font face="Verdana" size="6"><br>
</font></b><font face="Verdana" size="1">
<br>
<b>Starting in mid-2005 as the first Omani joint stock enterprise in the 
property development sector, Al Madina Real Estate Company SAOC today offers 
freehold properties to both Omanis as well as GCC nationals, reports Sunil Kumar 
Singh<br>
</b><br>
<img border="0" src="ccor.jpg" width="191" height="198" align="left" hspace="5" vspace="5">Pre-empting the market often pays dividends in business. The only rider is — the 
timing should be right. Take the case of Al Madina real estate company, part of 
the Al Madina group. Its dyed-in-the-wool sense of market judgement has helped 
create a real estate giant, which today has in its fold many mega property 
projects that dot the Muscat skyline.<br>
<br>
The company began its sojourn in 2003-04 as a start-up called Al Madina real 
estate LLC with a pilot project in Wadi Kabir area. Like other players rushing 
to jump onto the real estate bandwagon that was about to take off in the 
Sultanate, Al Madina too had a vision and it hit the bull’s eye. <br>
<br>
Says Abdul Rahman Barham, Chief Executive Officer of the company, “The Wadi 
Kabir project promised the onset of the real estate boom. We were convinced that 
it was time to take the plunge and do something bigger. It also made us realise 
that it was time to be more sophisticated, more organised, more 
institutionalised and add real value to the real estate sector. It was this very 
realisation that prompted us to form Al Madina real estate as a joint stock 
company in 2005.” <br>
<br>
He adds, “Omani real estate market is going to be a major contributor in 
economy. Earlier too it played a major role, but as part of the construction 
industry. But now we’re seeing a new full fledged sector evolving and that is 
the real estate development sector, getting away from the parent construction 
industry, having the potential to house huge assets and invite enormous foreign 
investments.”<br>
<br>
<b>Increasing Valuation</b><br>
Following the success of the Wadi Kabir project, there was no looking back for 
Al Madina. Capitalising on the real estate boom, its concerted entrepreneurship 
in real estate projects kept soaring. And so did its profile. Today, company’s 
products and services include real estate development, real estate investment, 
usufructs of government properties and property management. And not to speak of 
its shareholders that include government bodies as well as corporate bigwigs 
such as Al Madina Financial &amp; Investment Services, Ministry of Defense Pension 
Fund, KEMFIC, Qatar Fund, Trans gulf, Al Taman Company, BankMuscat 
International, and Diwan of Royal Court.<br>
<br>
Take a peek at the range of projects the company is managing today. Its Rihab Al 
Mawaleh housing complex, located in Al Mawaleh near Muscat City Centre, offers 
freehold property to both Omanis and GCC nationals. It offers a modern urban 
lifestyle with all modern amenities, such as temperature controlled swimming 
pool, multi-purpose hall for parties, gym, sauna, grassed playground, garden, 
landscaping maintenance, close circuit cameras, security card reader in the main 
gate, video phone facilities, high speed internet and so on.<br>
<br>
The company has also formed a special purpose vehicle (SPV)–Tilal Development 
Company–which is developing a RO 60 million project in Al Khuwair. Says Barham, 
who served as the General Manager of Salalah Flour Mills from 1997 to 2005, “We 
launched the project in November 2006 coinciding with the National Day, and the 
response to the apartments we put up for sale was overwhelming. Christened Tilal 
Complex, the project designed by an Australian architect and spread over a 
sprawling 77,000 sq.m, is planned in three phases. In phase one, construction of 
shopping mall (Muscat Grand Mall), offices, residential apartments and service 
apartments is planned. In the second phase, a five-star hotel will be built and 
in the third phase a residential condominium is planned.<br>
<br>
Adds Barham, “It’s a unique concept introduced in Oman, based on mixed use 
development, offering residential space along with super market, restaurants, 
food courts, offices, hotels, etc.” The ground floor area of the Muscat Grand 
Mall is planned to cover approximately 31,000 sq.m, providing department stores, 
grocery stores, retail shops, Arabian courts and covered car parks on two 
levels, while the upper floor area of approximately the same size provides 
department stores, retail shops, food courts, cafés, restaurants, Arabian 
courts, and entertainment, with covered car parks situated on two levels.<br>
<br>
The Tilal Offices complex offers 40,000 sq.m of space in four buildings situated 
over five levels on top of the shopping mall, equipped with high-tech 
facilities, IT infrastructure and office communication technologies. What’s 
more, one of the office buildings will be offering ‘serviced offices facilities’ 
with an international operator to provide world-class services for business 
people from Oman as well as the GCC. <br>
<br>
The Tilal Residence Unit offers 180 units, comprising one-bedroom, two-bedroom 
and three-bedroom ultra-modern apartments to Omanis and GCC nationals. On top of 
the shopping mall, food courts and entertainment area are planned 110 units of 
service apartments. And lastly, for health freaks, there will be a health club, 
spread over 1,000 sq.m with state-of-the-art equipment and facilities such as a 
gym, Jacuzzi, steam bath, temperature controlled swimming pool, etc.</font></p>
