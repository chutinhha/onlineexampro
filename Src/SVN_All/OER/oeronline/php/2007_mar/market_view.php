        <b><font face="Verdana" size="4">Back in the Bears’ Grip</font></b><font face="Verdana" size="1"><br>
<b>Last month, we were looking at MSM touching the 6K mark, but it went down by 
more than 3 per cent<br>
</b>
<br>
The period January 15-February 17 has seen one of intensified selling pressure 
with a declining trend in sector indices. The General Index fell by 3.07 per 
cent to close the month at 5712.27 as on February 17. The Services Index gained 
marginally by 1.19 per cent while the Banking/Investment Sector Index and the 
Industry Index declined substantially by 3.62 per cent and 9.68 per cent, 
respectively. However, looking at the YTD growth, we see that the Industry 
sector index was the only one to lose ground, declining by 2.28 per cent.<br>
<br>
The top five gainers during the month were Oman Education (up 113.9 per cent), 
Oman Hotels (30 per cent), Oman National Livestock (28.3 per cent), Dhofar 
University (17.2 per cent) and Muscat Gases (16.5 per cent). The top five losers 
were Flexible Industry Packaging (down by 92.7 per cent), Oman Cables Industry 
(17 per cent), AES Barka (15.1 per cent), Fincorp (14.1 per cent) and Al Hassan 
Engineering (13.6 per cent).</font><p align="center" style="margin: 3px 10px">
<img border="0" src="marketview.jpg" width="498" height="464"><p align="justify" style="margin: 3px 10px">
<font face="Verdana" size="1"><b>Other GCC Markets</b><br>
Saudi Arabia and Dubai were the only markets that held on to their gains in the 
period. Qatar emerged as the biggest loser, declining by 9.25 per cent, while 
the other losers in the GCC included the MSM (down 3.07 per cent), Bahrain (1.93 
per cent) and Kuwait (1.73 per cent). However, on a YTD basis, MSM and Dubai 
were the only two markets that to have improved, moving up by 2.34 per cent and 
2.16 per cent, respectively.<br>
<br>
<b>Saudi Market – An Investment Opportunity<br>
</b>After the strong growth seen in 2003-05, the Saudi market underwent a correction 
phase and ended 2006 at 7,933.3 points, down a whopping 52.5 per cent over the 
2005 close of 16,712.64 points. The biggest market of the GCC region was the 
biggest decliner in 2006. <br>
<br>
Its market capitalisation at the end of the year reached US$326.3 billion. SABIC 
continued to be the index heavyweight, accounting for 21.5 per cent of the total 
market capitalisation followed by STC and Al Rajhi Bank, which accounted for 
13.5 per cent and 10.6 per cent, respectively. However, the strong sell-off seen 
in the secondary market did not affect investors’ as well as corporates’ 
interest in the primary markets. Nine new companies got listed on the Saudi 
bourse, taking the total number of listed companies to 86. <br>
<br>
The market is trading at a P/E of 15-17x and some of the stocks look really 
cheap and will attract investors’ attention.</font><p align="center" style="margin: 3px 10px">
