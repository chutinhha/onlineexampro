			<td width="267" valign="top">
			<p align="justify"><font face="Verdana" size="1">The formula for 
			doing well at business looks quite simple on paper: Provide goods 
			and services that are superior in quality, sell them at a reasonable 
			cost and meet the customers’ needs beyond the transaction.<br>
			<br>
			<br>
			Commerce rarely plays out like this in the rough-and-tumble market 
			economy, of course. Customers struggle to differentiate between 
			product offerings and price points are rarely straightforward. 
			Companies fill the perception gap with marketing campaigns that can 
			obscure as much as they clarify.</font></td>
			<td>
			<p align="center">
			<img border="0" src="manag.jpg"><br>
			<font face="Verdana" size="1"><b>David Batstone</b><br>
