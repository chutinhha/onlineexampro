<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
<img border="0" src="manag1.jpg" width="200" height="157" align="left" hspace="6" vspace="4">The mobile phone industry exemplifies the confusion. The average consumer 
despairs when it comes to choosing among providers, unable to make heads or 
tails of that maze referred to as the ‘calling plan.’ The disconnect takes place 
at the level of expectation. For customers, a phone call is just that, a phone 
call. Mobile companies are trying to sell relationships, however, not phone 
calls.<br>
<br>
When customers try to cancel their subscriptions, most mobile providers slap 
them with a termination fee of $150 or more. Trying to force consumers into 
loyalty rarely works. Customers need a reason to believe in what a company does. 
They may respond to seeing an ad or using a product or talking with a company 
agent. Each point of contact cements or, alternatively undermines their 
emotional attachment to the company. Their loyalty only grows once their basic 
demands for trust, security, usefulness and fair exchange are met.<br>
<br>
<b>Quick assessment</b><br>
Whenever I meet senior managers of a company, I ask them four quick questions 
and their answers tell me a great deal about how well their firm takes care of a 
customer.<br>
<br>
1) What overt promises does the company make to customers and do those promises 
truly identify what it can deliver?<br>
2) Do the firm’s operations – from marketing to sales to fulfilment – respect 
the dignity of customers?<br>
3) When the company fails a customer, how fast does it make amends?<br>
4) Does the company make meaningful changes in response to customer needs and 
feedback?<br>
<br>
But is it really possible to control the quality of customer experience in a 
large corporation? A small business works hard to satisfy expectations because 
it cannot afford to lose a single customer. Most of us assume that after the 
company reaches a certain size, it loses its capacity to deliver personal care. 
Not Bill Pollard, the former CEO and chairman of the Service Master Company. “My 
goal is to bring the spirit of a small company and its responsiveness to 
customer needs into a big organisation,” he declares.<br>
<br>
Bill confronted the challenges of major growth, having steered a modest firm 
into a Fortune 500 corporation. When he arrived at Service Master in 1977, the 
company had annual sales of US$200 million; those figures had soared to over 
US$6 billion by the time he resigned as chairman of the board in early 2002.<br>
<br>
Even when Service Master was a much smaller enterprise, ensuring a high quality 
of customer care was not easy, says Bill. The biggest obstacle: Service Master’s 
workforce does not operate under one roof. ServiceMaster offers a wide range of 
commercial and residential services – home and office cleaning, lawn care, 
termite and pest control, plumbing, and furniture maintenance. Most of its 
employees work at the customer site, which means there is no direct line of 
management supervising them when they are delivering service.<br>
<br>
<b>Empower the provider</b><br>
<img border="0" src="manag2.jpg" width="300" height="203" align="right" hspace="6" vspace="4">The higher up the ladder one moves in a corporation, the less an individual may 
know about what the customer wants and needs. Because Bill firmly believes in 
the maxim, he aims to push decision-making to the people who are closest to the 
customer. A company operates at its peak when workers are empowered to take 
decisions and are measured by results. Take away that right and responsibility 
and customer care will suffer.<br>
<br>
A Harvard study featuring Service Master appears to justify Bill’s confidence. 
The study found that more than 50 per cent of the time when customers referred 
to excellent customer care, they cited an incident when a service provider had 
erred and yet corrected the error on the spot.<br>
<br>
People are imperfect and are going to make mistakes. Customers seem to 
understand that fact and do not expect perfection as much as they do 
satisfaction. Contrary to popular belief, a company does not score big points 
with customers when it calls two weeks later with the message, “Sorry, we messed 
up, so we’ll give you your money back.” Customers are more inspired by workers 
who show responsibility and are willing to make decisions to resolve a problem. 
Therein lies the real key to effective customer service.<br>
<br>
To encourage that kind of vitality at Service Master, Bill found that he 
constantly had to battle bureaucracy and an expanding midriff of middle 
management. A large corporation by its nature adds layers. Bill argues that the 
more layers a company has – and here he tips his hat to Peter Drucker – the more 
noise is generated that distracts workers and drowns out customer voice. “We 
constantly struggle to find a better way to flatten our firm,” he admits.<br>
<br>
<b>Direct avenues</b><br>
Bill also strives to shorten the distance between sales and operations. Service 
Master has a bias to keep customer acquisition close to where the delivery of 
the service takes place. A good example is how it handles its telemarketing 
teams. Most of its 500 branch organisations across the United States deploy an 
in-house telemarketing system. The company could have farmed out this operation 
to an outside vendor or centralised its call centres into a few national hubs. 
Any clever accountant could demonstrate the big cost savings of following either 
one of these paths.<br>
<br>
At times, focusing on partial financial figures can lead a company down the 
wrong path. Once Bill’s management team did a total cost analysis of its 
customer acquisition and retention, they discovered that keeping the call 
centres decentralised at local branch offices was a far more effective and 
cost-efficient strategy.<br>
<br>
The reason can be summed up in one word: accountability. When branch managers 
arrive at the office each morning, they know immediately whether the previous 
evening their call centre had reached its quota for new customer acquisitions. 
If quotas are not met, the burden for figuring out the why falls squarely on the 
manager’s shoulders.<br>
<br>
Imagine the alternative – how easy it would be to blame poor performance on an 
anonymous call centre. At Service Master, the same branch office that brings in 
the customer also delivers the service and follows up the relationship. It all 
takes place in one location close to the customer.<br>
<br>
When a large enterprise keeps accountability close to the point of delivery, its 
size becomes less of an obstacle. Bill Pollard’s philosophy makes lots of sense 
for any size enterprise: “Build trust with your customers, respond to their 
evolving needs and empower your people to correct mistakes when they occur. Do 
these three things and you will create an environment for long-lasting customer 
relationships to grow.”
