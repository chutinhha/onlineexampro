<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
</font><b><font face="Verdana" size="4">Creating Loyal Customers</font></b><font face="Verdana" size="1"><br>
<br>
<b>Only those companies that work hard to create relationships and build trust 
with their customers can succeed in a market economy</b><br>
&nbsp;</font><div align="center">
	<table border="0" cellpadding="0" style="border-collapse: collapse" width="95%">
