</font><b><font face="Verdana" size="4">Lorena Ochoea Kicks Off 2007</font></b><font face="Verdana" size="1"><br>
<b>Mexico’s Lorena Ochoa has slowly but surely loosened Annika Sorenstam’s 
stranglehold on the women’s game, and their battle for supremacy is likely to be 
a major factor on the 2007 LPGA Tour</b><br>
<br>
<img border="0" src="golf.jpg" align="left" hspace="6" vspace="4">Lorena Ochoa is back! <br>
<br>
Ochoa, however, is more than just another player. She won six tournaments last 
season en route to becoming the first player to win the LPGA Tour Player of the 
Year since 2001. <br>
<br>
Ochoa earned US$2,592,872 last year, more than double her 2005 earnings, to win 
the money title and become just the second player in history to surpass the 
US$2-million mark in a season. Ochoa, 25, won six times last year to capture the 
Vare Trophy for the lowest scoring average and LPGA Tour player of the year, 
ending Annika Sorenstam’s five-year reign. She also was voted AP Female Athlete 
of the Year, a first of its kind for a Mexican athlete. She had six second-place 
finishes, including a runner-up at the SBS Open, and 20 top-10 finishes while 
leading the tour in birdies, eagles, rounds in the 60s and greens in regulation. 
Last year, Joo Mi Kim got her first LPGA Tour win with a birdie on the second 
playoff hole to beat Ochoa and fellow South Korean Soo Young Moon.<br>
<br>
By playing in the season-opening SBS Open in Hawaii, the Mexican has gained a 
head start on her Swedish rival. Sorenstam will not enter the fray until March, 
just in time for the first major, the Kraft Nabisco Championship.<br>
<br>
Ochoa is a national hero in her native Mexico. She recently opened two golf 
academies with plans for a third. The Mexican government awarded her the 
National Sports Prize, its highest honour for an athlete.<br>
<br>
Fellow islander, Michelle Wie, has a left wrist injury that will keep her away 
from golf for at least a month. Wie finished tied for second in the inaugural 
SBS Open in 2005 and skipped last year’s event. While the SBS Open begins play 
for most of the field, Karrie Webb already seems to be in midseason form. The 
32-year-old Australian is coming off one of her best seasons with five titles 
including the Kraft Nabisco Championship, where she beat Ochoa in a playoff. 
Webb went over $2 million in earnings for the first time to finish second on the 
money list to Ochoa. <br>
<br>
All that remains for Ochoa is a major victory. She wishes to become No. 1, and 
then end her golf career after 10 years.</font><p align="justify" style="margin: 3px 10px">&nbsp;<div align="center">
