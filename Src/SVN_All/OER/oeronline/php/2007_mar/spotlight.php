<p align="justify" dir="ltr"><font face="Verdana" size="3" color="#FF0000">
<br>
</font><b><font face="Verdana" size="4">Realty Boom</font></b><font face="Verdana" color="#FF0000"><br>
</font><font face="Verdana" size="1"><b>Pushed by a high demand, increased 
government and private sector investment and young demography, the real estate 
sector in Oman is expanding at a scorching pace. OER reports<br>
</b></font><font face="Verdana" color="#FF0000">
<br>
</font><font face="Verdana" size="1">
<img border="0" src="spot1.jpg" align="left" hspace="6" vspace="4">In 
the last few years, Oman’s GDP has grown at a very high rate thanks to increased 
oil prices. In 2006, the GDP crossed the RO13.6 billion-mark. In 2003, it was 
just RO8.3 billion. The public finance generated a surplus of RO3 billion in 
2006. The positive market sentiments along with major increase in government’s 
income have resulted in development across various sectors, including real 
estate. <br>
<br>
<b>Government Setting the Pace</b><br>
The Government’s distribution of land went up by three times in 2004 compared to 
2003 figures and in 2005, it has more than doubled from 2004. The distribution 
growth is not restricted to Muscat alone, but is spread across most of the 
governorates and regions. The maximum growth has been seen in residential and 
residential-cum-commercial units. Compared to the distribution of just 10,294 
residential plots in 2003, the Government distributed 62,450 plots in 2005, a 
jump of over 600 per cent! (See graphs: No. of Plots Distributed by Governorate 
&amp; Region and No. of Plots Distributed by Type of Use.) <br>
<br>
The liberalisation policy adopted by the government over the last few years has 
also given a good thrust to the real estate sector. The Central Bank of Oman’s 
move to allow commercial banks to lend 5 per cent of their total portfolio for 
housing purposes has resulted in a flood of housing loan options. The rate of 
interest has gone down steeply, making housing loans an attractive proposition, 
especially at a time when land rates are appreciating very fast. Alliance 
Housing Bank is offering housing loans at a rate as low as 6.55 per cent. Other 
banks are offering a marginally higher rate.<br>
<br>
The Royal Decree 12/2006 has made it possible for foreigners (including non-GCC 
nationals) to own property in Oman. Though ownership rights are restricted to 
certain tourism-designated areas, this has given a fillip to mega projects, such 
as The Blue City and The Wave. Non-Omani buyers and their immediate family 
members are entitled to special residence visas. </font></p>
