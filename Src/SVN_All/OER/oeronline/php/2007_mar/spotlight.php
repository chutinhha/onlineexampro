<p align="justify" dir="ltr"><font face="Verdana" size="1">
<br>
<br>
<b>Drivers of Growth</b><br>
<img border="0" src="spot4.jpg" width="279" height="221" align="right" hspace="6" vspace="4">Almost 40 per cent of Oman’s 2.5 million-plus population falls in the 20-39 year 
age group. Recent trends have shown that this age group has a greater degree of 
desire to purchase or build a house of their own. The large size of this age 
group is already leading to a higher demand for plots and houses. This trend is 
seen continuing in the coming years. Omanis have realised that real estate rates 
in the Sultanate are very low compared to other Gulf states, such as UAE and 
Qatar. The meteoric appreciation seen in other markets will be replicated in 
Oman in the short to medium term. Therefore, an increased number of Omanis are 
keen to invest now and build a real estate asset to secure their future. <br>
<br>
Another important factor is the growing population of expatriate workers. By the 
end of October 2006, their number had grown to 489,350 compared to 424,788 by 
2005 end. As most of them rely on rented accommodation, an increase in their 
numbers has led to a greater demand for residential units. But as demand for 
more residential units has not kept pace with supply, it has resulted in a steep 
increase in rentals in areas such as Muscat and Sohar. Taking Muscat as an 
example, today, it is extremely difficult to rent even a one bedroom flat in 
areas such as CBD, Darasit and Wadi Kabir even at a rate for which one used to 
get a two-bedroom flat in 2005! Overall, as per market estimates, the rentals 
have gone up by 20-40 per cent in Muscat itself. And the occupancy rates have 
touched 98-99 per cent.</font></p>
<p align="center" dir="ltr">
<img border="0" src="spot5.jpg" width="500" height="371"></p>
<p align="justify" dir="ltr"><font face="Verdana" size="1">
According to a report on real estate by Al Madina Financial and Investment 
Service Co. (SAOC), land prices have witnessed a major increase following a 
major buying spree, especially by citizens of several GCC countries (other than 
Oman). Buying has generally been on account of speculative purposes. The report 
further says that an interesting trend being witnessed over the recent past has 
been the increase in prices of land close to tourism/industry-designated areas, 
such as Azaiba (The Wave) and Sohar (Industrial Hub). (See Table: Land Rates in 
the Sultanate of Oman.)<br>
<br>
<img border="0" src="spot6.jpg" width="300" height="200" align="right" hspace="6" vspace="4">On the industrial front, Sohar is witnessing the maximum activity, becoming the 
industrial hub of Oman. Investments valued at US$12 billion have been lined up 
for Sohar. Mega projects such as Sohar Aluminium, Sohar Refinery and Sohar 
Methanol are not only going to add substantially to the Sultanate’s 
manufacturing kitty but are also leading to a multiplier effect on the real 
estate market through higher demand of land for industrial, housing and 
commercial purposes. <br>
<br>
<b>Mega Integrated Tourism Projects</b><br>
Tourism has been identified as a major focus area in the Government’s Vision 
2020 plan. As a result, Oman is witnessing major investments in the tourism 
sector. In the beginning of 2006, Shangri-La’s Barr Al Jissah Resort &amp; Spa was 
launched in the Sultanate. <br>
<br>
The Wave is at the forefront of number of mega projects coming up in the next 
few years. It is a world-class US$1 billion beachfront tourism and residential 
project, stretching along 6 km of natural beach. It will include a string of 
man-made lakes and islands. The development will encompass a signature 18-hole 
green links golf course designed by renowned golfer and designer Greg Norman, a 
300-berth marina, four luxury hotels, retail and restaurant outlets along with 
approximately 4,000 residential units. It recently made sales bookings for its 
second phase for a combined value of US$85 million, with units priced between 
RO115,000 and RO450,000.<br>
<br>
The Blue City, estimated to cost US$15-20 billion, is the most visionary urban 
and lifestyle development project in the region. When finished, it will embrace 
approximately 34 sq km at Al Sawadi. It will house around 250,000 inhabitants. 
Together with its surrounding areas, it will have the capacity to absorb and 
serve up to two million tourists a year. Phase I will see three five-star hotels 
each with approximately 200 rooms; one golf resort hotel with a double 18-hole 
championship signature golf course along the creek; a tourist village with a 
contemporary souq, heritage museum and 1,000 apartment units; 1,200 
villa/apartment units integrated in the golf course park; and a number of public 
and semi-public attractions, including a waterfront amphitheatre. A boutique 
hotel designed, equipped and managed by world-famous London hotelier Gordon 
Campbell Gray is also going to be built at the Blue City.<br>
<br>
The Government-owned Oman Tourism Development Company (OTDC) and Egypt’s Orascom 
have also finalised the master plan for the prestigious US$600 million joint 
venture – Muriya. It proposes to transform Qurum into a classy township by 
upgrading its status as the hub of the city, involving an elegant 250-room six 
star hotel, an Omani souq linked to a 35,000 sq.m shopping mall, a 35,000 sq. m 
office building to house reputed local and foreign companies, and a retail 
pedestrian street – promenade – packed with exclusive brand outlets, cafes and 
restaurants, a six-screen cinema theatre, and underground parking garage, to 
name a few.<br>
<br>
The Muscat Golf &amp; Country Club, under development and close to Seeb Airport, 
consists of an 18-hole mountainous golf course, luxurious residential 
accommodation as well as other facilities.<br>
<br>
Among the other new tourism projects, the Saud Bahwan Group is expected to 
implement a project at Jebel Al Akdhar. Similarly, a Qatari company is 
implementing a project in Ras Al Hadd. Duqm will also add a hotel in the near 
future. The Shinas and Yitti tourism projects are expected to kick-start this 
year.</font></p>
