              </font></b><b><font face="Verdana" size="1"> The MSM continued its upward trend in January as the Index rose by one per cent largely on account of trading in bank securities</font></b><br>
                  <font face="Verdana" size="2"><b><br>
                  </b> </font><font size="2" face="Verdana">The Muscat Securities Market (MSM) Index rose by nearly one per cent at 194.21 points in January, pushed up by investor speculations that banks may post healthy profits in 2002.&nbsp;<br>
 Oman International Bank (OIB) gained 290 baisas during the month to RO1.840, while Alliance Housing Bank (AHB) added 100 baisas more to its share price of 950 baisas. BankMuscat, Oman's largest bank, managed to gain 60 baisas to touch RO3.850.&nbsp;<br>
 Securities that suffered marginal decline during the month were Dhofar International Development and Investment Holding, which lost 30 baisas to reach RO1.840. Salalah Flour Mills and United Power Company each dropped 90 baisas to reach RO1.810 and RO1.300 respectively.&nbsp;<br>
 AHB has already announced a net profit of RO1.700 million for 2002 paying out a cash dividend of 4.5 per cent to its shareholders. Investors have projected that BankMuscat will announce a net profit of about RO20 million for 2003 while OIB in the region of RO5 million. But traders warned that investors should not rely on the banking sector's performance to make investment decisions.&nbsp;<br>
 Banks are still logged down by loan loss provisions and they will take a while to completely recover. The best advice is to diversify investment to the service and industry sectors since they will be the main recipients of government funds allocated in the 2003 budget, said Rajat Dewan, analyst with at Gulf Investment Services.&nbsp;<br>
 Oman announced a budget of RO3 billion for this year with an investment expenditure of RO614 million, which the government plans to spend on infrastructure including roads, health and education services.&nbsp;<br>
 In mid-January, Oman has awarded three major projects totalling US$1.023 billion. A Japanese consortium of JGC Corp and Chiyoda Corp was awarded a US$879 million contract to build Sohar refinery. In the same month, South Korea's SK Engineering won a US$99.5 million contract to maintain and operate refinery.&nbsp;<br>
 The government also approved the construction of a Sur-Quriyat road to cost around US$45 million to build.&nbsp;<br>
 It is good that the government has awarded these projects very early in the year. It will help maintain investor confidence in the MSM, Dewan said.&nbsp;<br>
 The MSM, the Gulf's smallest bourse with a market capitalisation of about RO1.5 billion, closed last year 26.2 percent higher at 191.86 points compared to 152.08 points at the end of 2001 boosted by improved transparency regulations and good corporate results.<br>
              The reforms introduced by the bourse watchdog Capital Market Authority (CMA) last year have succeeded in restoring a measure of confidence in the market, which lost 24.4 per cent in 2001.&nbsp;<br>
