            <img border="0" src="helm-1.jpg" align="left" hspace="5" vspace="5" width="200" height="276">Striking the right note&nbsp;<br>
 Raphael Parambi, CEO, Muscat Finance Company, talks about his management philosophy, leadership and career to Janaki Raghavan in an open interview&nbsp;</b></font>
              <p><font face="Verdana" size="2"><b> Career path</b><br>
              I started my career in 1977 in India soon after completing my management studies. After ten years I came down to Oman in 1987 to undertake a feasibility study for setting up a financial services company. Muscat Finance Company
              (MFC) was started in December 1987 and I took over initially as the general manager, and was later promoted as chief executive officer.&nbsp;<br>
              <br>
              <b> MFC's growth&nbsp;</b><br>
 Earlier, our business was confined to only car finance. We later branched out into equipment leasing and pioneered the concept of financing for consumer durables with our 'Home Plan' scheme. Our other firsts include factoring, and working capital requirement and joint sector project abroad.&nbsp;<br>
              <br>
              <b> Management culture&nbsp;</b><br>
 At MFC we have an open style of functioning. I am against rigid departmentalisation, and I think that the staff should share responsibilities across the company. We organise six staff meetings and six branch meetings every year to streamline our functioning. We also have a staff-voted 'Best Employee' award every month with a cash incentive and certificate. We have made it a policy to ensure that every staff member has an equal chance of winning and so an awardee is eligible for the next award only after a one-year period. However, our informal set-up is not to say that we compromise on deadlines or results. We are serious when it comes to business. We also believe in providing maximum opportunities for competent Omani youth, and we have local staff at all levels across the company.&nbsp;<br>
              <br>
              <b> Leadership style&nbsp;</b><br>
 I prefer open communication. I am accessible to all the staff and my door is always kept physically open. I regularly interact with each staff member on a one-to-one basis and I am a great believer in teamwork. I am very fastidious as far as quality of service is concerned and so I have a tendency to do too many things myself. My perception is that I don't delegate enough and this costs me a lot of time. But, I suppose, this is how we are able to deliver consistently.&nbsp;<br>
              <br>
              <b> Biggest strength&nbsp;</b><br>
 I can pragmatically analyse emerging opportunities and the problems that go with it. When I came here to establish a financial services company I was told that it would not be viable because of the credit behaviour. But the research confirmed that there was immense potential and I was optimistic and decided to give it a try. I am also willing to listen to people and talk to them as a team but since collective decisions are not possible all the time, I take the responsibility for the final decision.&nbsp;<br>
              <br>
              <b> Biggest mistake&nbsp;</b><br>
              Fortunately, I have never done anything that has gone drastically wrong. I of course do make mistakes for if I don't it only means that I am heading nowhere. But I am ready to admit my mistakes and prepared to learn from them.&nbsp;<br>
              <br>
              <b> Biggest weakness<br>
              </b>I don't delegate enough. I am also impatient for results and this does pressurise my people. But I have found out that they emerge stronger when confronted by challenges.&nbsp;<br>
              <br>
              <b> Biggest achievement&nbsp;</b><br>
 Setting up MFC is my biggest accomplishment to date and it has given me immense personal satisfaction as well. The scenario was rather discouraging at the time of establishing the company and soon after that the Gulf war broke out compounding the problems. Competition also became rather stiff with multinational banks stepping in and one bank openly threatened to wipe us out. Not only did we prove our detractors wrong, but we have also been consistently recording the highest profits in the industry for the past 15 years.&nbsp;<br>
              <br>
              <b> Biggest regret&nbsp;</b><br>
 The lack of adequate opportunity to learn anything new has always bothered me. Here learning is a big struggle and it is a big challenge to update oneself.&nbsp;<br>
              <br>
              <b> Trade secrets&nbsp;</b><br>
 We do not set up the targets for our staff and this approach is unheard of here. The trust that we repose in them makes them feel committed to achieving their own self-imposed targets. We also believe in transparency in operations. MFC is a pro-active company and believes in looking ahead. We can anticipate problems quite successfully and since forewarned is forearmed we combat them effectively. We invest sizeable resources on training our staff on an on-going basis. MFC sponsored my management studies at Harvard.&nbsp;<br>
              <br>
              <b> Customer care&nbsp;</b><br>
 Being a customer-driven organisation all operations of MFC have been designed with only the customer interest in mind. Every customer is valuable. We conduct exit interviews with every good customer to obtain a review and feedback and take corrective action if required. Our high-repeat customer base is an indicator of our levels of service.&nbsp;<br>
              <br>
              <b> On competition&nbsp;</b><br>
 Competition energises us and brings out the best in us. Our strategy to tackle competition is to take them by surprise.&nbsp;<br>
              <br>
              <b> Future plans&nbsp;</b><br>
 It is 15 years since we have commenced operations. It is now time for us to reflect as well as looking forward. We have started diversifying, and our thrust areas now include corporate financing for both small and medium enterprises and exploring more overseas joint ventures. At the personal level I hope to take up social service on a full time basis after retirement.&nbsp;<br>
              <br>
              <b> Family&nbsp;</b><br>
 I am happily married to Meera, an ex-banker, for 20 years. We have three children. Divya (18) is doing her graduation in USA, Rohan (16) studies for Baccalaureate and Anisha (14) is doing her schooling.<br>
              <br>
              <b>Hobbies</b>&nbsp;<br>
 We are a family of sports enthusiasts. I am into competitive swimming, squash and badminton. All of us enjoy tennis and all my children are accomplished sports persons.</font></p>
