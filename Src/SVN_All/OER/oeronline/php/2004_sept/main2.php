<p align="justify"><font face="Verdana" size="4" color="#FF0000">Adding Oomph to 
your office<br>
</font><b><font face="Verdana" size="1"><br>
Putting in place a smart office that incorporates cutting-edge technology and 
state-of-the-art products has become a key focus area in the agenda of most 
corporates. An OER overview.</font></b></p>

<p align="justify"><font face="Verdana" size="2">
<b>The Executive's guide to working smart</b></font><font face="Verdana" size="1"><br>
<img border="0" src="im3.jpg" align="left" hspace="5" width="220" height="155">As 
an increasing number of organisations are waking up to the fact that the 
equipment, automation products and technologies that they use at the workplace 
play a pivotal role in enhancing work efficiency and influencing the performance 
graphs, putting in place a smart office that incorporates cutting-edge 
technology and state-of-the-art products has become a key focus area in the 
agenda of most corporates. <br>
Not to be left behind, businesses in the Middle East too are getting their act 
together and the heightened awareness levels are stoking the demand for the 
latest and best the industry has to offer, writes Janaki Raghavan </font></p>

<p align="justify"><font face="Verdana" size="1">
While they may not exactly be going through the roof, the sales figures of 
Oman's office equipment and enterprise technology segments have not been doing 
too badly. Industry experts mention that Oman's IT products market is around 
RO85 million while the office automation products have a value of RO22 million 
and office furniture has a market of RO9 million. <br>
<br>
&quot;The Sultanate's government constitutes the largest market for IT &amp; Office 
Automation accounting for 60 per cent in most product segments including IT 
products. The market grows in direct proportion to the availability of funds and 
the sanctioned budgets of major ministries. Last year, Office Automation 
products market grew by 8-10 per cent while the market for IT products increased 
by over 20 per cent,&quot; informs Sanjeev Mahajan, general manager, Mustafa Sultan 
Office Technology Company. <br>
<br>
Computers top the list of the various hardware equipment required in a smart 
office. The Sultanate's PC industry in the current quarter has registered a 49 
per cent growth compared to the same period last year, with laptops growing at 
79.1 per cent in the same period. In terms of business segments, Small and 
Medium Enterprises (SME) continue to command the largest share (30%), says a 
reliable source from the industry. <br>
<br>
'The market has grown in the last 5 years. With many projects being executed, as 
well as being planned, the various segments that contribute towards making an 
office efficient are growing at a rapid pace. Even though the growth is modest 
compared to global levels as far as the Middle East is concerned, Oman is one of 
the countries in the forefront with regard to development in the Information 
Technology (IT) and Information Communications Technology (ICT). Technology in 
the office is playing an increasingly important role, as businesses in the 
Middle East look to automate processes and provide remote wireless access to 
employees, allowing them to work from almost anywhere,&quot; declares a market 
analyst. Are you tech-smart? Being tech-smart means that your office should be 
making good use of technology to achieve your project's goals, and making 
efficient use of resources in order to work smarter and more productively. The 
benefits to being tech-smart include saving time and money, enhancing 
communications, tracking project details and projecting a professional image.<br>
<br>
'Looking back at the development of businesses in the region over the past two 
decades, the average company has come a long way from being a workplace where 
only a couple of people had access to a computer to being one in which a desktop 
PC has become a mandatory apparatus on every office worker's desk. PCs that were 
essentially single function tools have graduated to the level of multi-tasking 
kits,&quot; says Krishna Murthy, general manager, Acer Computer (M.E.) Ltd. <br>
<br>
Reiterating this observation, B.C. Fernandes, general manager, Loay 
International LLC, says, ''Advances in digital technology have heralded the 
arrival of the integrated office environment, where equipment is linked 
together, where your computer network interfaces with an array of other 
equipment around the office. With the proliferation of multifunction devices in 
the corporate workspace, the need for central management of networked and 
integrated devices has become even more important.'' With this in mind, hardware 
majors such as Acer have developed a range of desktop, notebook and tablet PCs 
that offer maximum functionality, enabling workers across the region to work 
smartly both in and outside the office environment. <br>
<br>
For tech-savvy customers in the region's burgeoning corporate market, Acer has 
recently launched the Veriton GT models. The trend for constant access to 
information has also prompted players in the field to expand and develop their 
range of mobile computing options in the region. ''The overall notebook market 
in the Middle East is growing in excess of 50 per cent year-on-year,'' says 
Krishnamurthy. <br>
<br>
<b>Changing face of office technology </b><br>
There have been significant changes in technology in mobile computing as well as 
in mobile handsets. Power of mobile computing combined with developments in 
wireless networking and ultra-portable yet powerful handsets offer intriguing 
possibilities for modern executives. ''Motorola's handset MPX 200, manufactured 
in collaboration with Microsoft, lets one duplicate laptop facilities in handset 
and vice-versa. This enables executives to seamlessly walk in and out of offices 
without losing touch with urgent data,'' says Jayanta Borpujari, divisional 
manager, Khimji Ramdas Computer &amp; Communication Systems. Similarly, hotspot 
technology enables an executive to connect to the Web from his mobile laptop 
anytime anywhere. New powerful tools in document imaging have also contributed 
to the realisation of a smart office. With powerful OCRs and search techniques, 
latest versions of document management softwares introduce remarkable 
intelligence in offices as far as archiving and retrieving of documents are 
concerned. <br>
<br>
With the breed of globetrotting executives growing by the day, technology that 
not only allows travelling with all the business data intact, but also 
facilitates easy access of presentations and data files has become a common 
acquisition. Synchronizing data with auto sync software is another hot concept. 
Sony RME (Recording Media &amp; Energy ) has introduced Sony Micro Vault Pro - 2 GB 
- a USB media storage device for power users. Its compact chassis contains a 
1-inch hard drive that holds up to 2 Gigabyte of data. <br>
<br>
A tech-smart office should definitely include a LAN (Local Area Network), opine 
experts. Apart from connecting various computers and enabling 
information-sharing, LAN ensures easy back-up of data and has proved to be 
cost-effective by virtue of having a common printing and imaging solution. <br>
<br>
With processor speeds galloping every day, Dell has recently introduced the 
GX-280 series of Optiplex machines, which work on the different 91G Express 
chipset, further enhancing the processor speed. This latest offering also has 
dual channel DDR2 shared memory.<br>
<br>
The availability of wireless interfaces with very high bandwidth at affordable 
prices combined with the fall in prices of laptops and notebook PCs plus easy 
and quick deployment without need for cabling have fuelled the demand for 
wireless LAN solutions.<br>
<br>
Other new technologies penetrating Oman's market include ATM, VoIP, Call Centre, 
GPRS and ADSL. The substantial growth in the Fiber Network and the increasing 
availability of Bandwidth is expected to drive ATM traffic. <br>
<br>
''Lucent Technologies' VPN Firewall portfolio offers carrier grade, high-return 
security, VPN, bandwidth management and QoS solutions to suit small-office 
through network-edge requirements,'' claims M. Rajasekaran, head - 
Telecommunications, Areej Business Systems Co. LLC <br>
<br>
As a natural corollary, products that are compatible with these technologies 
have found a growing user base. Pocket PCs, mobile phones with camera, 
multimedia and PC capabilities, 3G mobiles, ruggedised PCs for outdoor mobility, 
and firewalls for network security have all become popular buys now. <br>
<br>
''The latest products that are being launched stand at two ends of the product 
spectrum. Low-end products have also become digital and multifunctional and 
offer more functions/features at even lower prices. This entry level segment has 
grown to account for 20-22 per cent of the requirement for IT/office equipment 
in the government sector and small &amp; medium business establishments. At the 
other end are high-speed, top-end feature rich and customised premium priced 
products that meet the exact requirements of specific market segments. The Oman 
launch of these high-end products almost coincides with their global launches 
since product life cycles are becoming shorter. The new buzz words even in Oman 
are convergence, connectivity, colourisation and customisation,'' declares 
Mahajan. <br>
<br>
''IP Communications is making inroads into enterprise networks. The compelling 
reasons for adoption of next generation networking technology are savings on 
costs, future-proofness, inter-operability &amp; adaptability with legacy equipment, 
and building a platform which will be the gateway to converged communications,'' 
opines Ajay Kapoor, general manager, OHI Telecommunications Co. LLC. <br>
<br>
Capitalising on the growing requirement for integrated communications solutions, 
players in the industry are fast introducing solutions with cutting-edge 
technology. ''The Alcatel Omni PCX Enterprise, an integrated communications 
solution for medium &amp; large corporations is an ideal platform for cost-conscious 
companies on the cutting edge. It combines the best of the traditional TDM 
telephony with the new native IP platform and support for Session Initiation 
Protocol. Embedded unified messaging, Call Log, ML interface, openness to 
tailor-made applications, blue-tooth capable IP phones &amp; support for WiFi &amp; WLAN 
are some of its key features. The Omni-PCX Office on the other hand is an 
'all-in-a-box' system targeted at the voice, data &amp; internet needs of small &amp; 
medium enterprises on a common platform&quot; says Sanjay Dua, head, Enterprise 
Networks - Sales, OHI Telecommunications DECT, a wireless communication 
technology, is fast becoming a worldwide standard for short-range cordless 
mobility. the fact that it can be adapted for many other cordless applications 
such as wireless local loop, cordless PBX etc, has contributed to its mounting 
usage. ''Interest in the DECT standard is global. Offices, warehouses, 
hospitality segments are all now looking at wireless communications equipment 
and the demand for this technology is set to boom,'' asserts Devan Thuppali, 
assistant general manager, OHI Electronics. <br>
<br>
<b>Towards a paperless office</b><br>
A few years ago, there was a babel of voices proclaiming that paperless offices 
were soon going to become the order of the day. Now, the clamour has died with 
people realising that it was just wishful thinking. ''It's about time that one 
understands the reality that only less-paper offices exist, and this can be 
achieved with appropriate document management,'' says P. Vidyasagar, director, 
SITCO LLC. Efficient document management calls for photocopiers and fax machines 
that are multifunctional. ''Konica Minolta's CS Digital technology fulfils this 
requirement as it handles multiple functions without sacrificing quality,&quot; says 
Ashish Srivastava, general manager, MHD LLC. <br>
<br>
'The Kyocera Mita range of multifunctional devices (MFD) and copiers are 
standardising their user-interface across all product segments to enable maximum 
compatibility for each product and service,&quot; says Mahajan.<br>
<br>
'Solution providers can implement a Rex-Rotary digital copier as the corporate 
fax server in the LAN/WAN to deliver faxes into the recipient's email, thereby 
drastically reducing the use of paper. This solution is neither expensive nor 
difficult to implement,'' claims Jaiprakash of SITCO LLC. <br>
<br>
<b>Office Telephony surges ahead</b> <br>
In synch with the scenario the world over, IP Telephony will endeavour making 
major inroads into the enterprise this year the world over and in the country 
too. <br>
<br>
'Philips Sopho iS3000 IP Enabled PABX Systems, offer both TDM and IP facilities 
and smooth migration to the native IP, which is the future,&quot; mentions 
Rajasekaran.<br>
<br>
Right furniture has also been identified as another key contributor to enhanced 
productivity and while choosing the right furniture, ergonomics has become the 
key operative word. ''Sales in the furniture industry, especially the office 
furniture segment, is linked to new projects that are undertaken by large 
corporate houses and the government. If some of the major projects that are on 
the cards are put in action in the near future, they can generate increased 
uptake of office furniture and we can see some growth in the hitherto more or 
less flattened growth graphs,'' mentions J.S. Charles, general manager, Fahmy 
Furniture. <br>
<br>
<b>Future Fortunes</b><br>
''In 2004, we expect higher business as oil prices have peaked. Growth 
expectations are upto 40 per cent in key IT segments and 20 per cent in office 
automation. Since the interior regions are in centrestage in terms of 
infrastructure development, the momentum will be higher there. However for 
Oman's companies to grow to a respectable size in the regional or global 
context, they have to look beyond Omani borders to faster growing markets in 
neighbouring countries and they have to partner with local companies there to 
exploit opportunities in new fast-growing markets,'' says Mahajan.<br>
<br>
In the words of Rajasekaran, ''ADSL services are expected to grow at a much 
faster pace. The growth in the ADSL service will lead to VPN solutions. 
Substantial growth is expected in ATM traffic and Video Conferencing. GPRS &amp; MMS 
services will grow much faster than GSM services. With the availability of very 
economical handsets and with the second mobile operator expected to start the 
services during 2005, the tariffs will be more competitive. The potential for 
growth will be very high.&quot; <br>
<br>
''The Oman market is very competitive. However, the Omani buyer places value 
over money when it comes to choosing critical office automation products and 
that has relegated many cheap or poor serviced brands to the background. So, the 
competition is between the fewer 'live' participants for a piece of action of 
the relatively small market size. The market size may change due to buoyant oil 
prices that could hopefully lead to increase in government spending, says Ajay 
Kaul, Senior Manager (Ricoh), Muscat Electronics. <br>
<br>
''With major drives from authorities to IT-enable Omani society, we see a 
significant growth in the industry in next few years. Once e-governance projects 
are in place, there is bound to be a cascading effect on overall market in terms 
of automation and so called smart offices,&quot; summarises Borpujari. </font></p>

