<p align="justify"><font face="Verdana" size="4" color="#FF0000">The Brit 
wanderer</font><font face="Verdana" size="1"><br>
<br>
<b>The new Rover 75 is a credible luxury contender with retro touches. It's a 
car that is well designed, well built, drives beautifully, and oozes class, 
writes Vicky Kapur</b></font></p>

<p align="justify"><font face="Verdana" size="1">
<img border="0" src="im17.jpg" align="left" width="225" height="107">Imagine a 
British car company, which has recently moved from German ownership, building a 
car with British styling and interior design. The result of that dream-come-true 
combination is the Rover 75. It was developed by BMW when it owned Rover. 
Indeed, it was all but launched under the BMW umbrella when the German company 
divested itself of its UK operations just weeks before the 75 was due. <br>
<br>
Rover may not be a very famous modern-day brand in the Sultanate, but the 75 
compensates for that with its style and equipment. The craftsmanship is 
immediately apparent. You get the cold feel of real metal door handles and an 
interior trimmed in wood and leather. <br>
<br>
Some of the colour combinations are overwhelming, but there's no denying its 
originality in a world of look-alike carbon-fibre and grey plastic interiors. 
The New Rover 75 C1, at RO7995, boasts of a 2.5-litre V6 engine with 180 BHP, 
stylish new halogen lamps, front, side and head-protection airbags and anti-lock 
brakes along with keyless entry and automatic windows. <br>
<br>
The 75 is not an overtly sporty car; it's more traditionally British in the way 
it goes about its business. It has a smooth ride and highly impressive noise 
control, but handling is further down the priority list. The steering is 
accurate although not over-endowed with feel. The brakes are powerful and 
communicative. <br>
<br>
<b>The outdoors </b><br>
The first thing that hits you about the new Rover 75 is retro-styling. Chrome is 
everywhere. Along the waistline, including the door handles, along the sills, 
chrome door mirrors and even a little oval badge on the lower edge of the front 
doors, declaring the engine size...very retro! <br>
<br>
The body looks and feels well constructed. In corners, it feels rigid, giving 
some consistency to the car's line. Outside, an elegant chromed grille, gold 
badge and downward-curving boot give the Rover 75 a distinctly Buckingham Palace 
effect that's not quite done justice in photographs. It has to be seen in the 
flesh to elicit a 'wow!' <br>
<br>
Size-wise, the Rover is between a BMW 3 and 5 -- so, cabin space is generous, 
and a pair of six-footers can sit in the back comfortably with their knees 
lightly touching the backrests of the front seats. Although the dynamics are 
sound, the suspension is too soft to be in any way sporting. Push the 75, and it 
understeers severely on slow, tight bends, but does not wallow or lurch. 
Outright grip has been traded for a softer ride. The Rover acts as everyday 
transport, as well as being used for weekends away. So it doesn't have the best 
pick-up in the world, but it suits the way we drive here in Oman. And the way it 
looks is just lovely. <br>
<br>
<b>The indoors </b><br>
The 75's qualities announce themselves as soon as you open the door. In the 
Rover 75 C1 automatic model that I tested--it has speckled velour instead of the 
cowhide of the dearer C3--there's something stiff-upper-lip that's lacking in 
the corporate techno-elegance of a German sedan. <br>
<br>
Every carmaker uses wood and leather these days to embellish their top models, 
but the Brits just manage to instil it with attitude. An upper-crust feeling 
resonates in the Rover's super-polished wood and elegantly chrome-rimmed, 
retro-look dials. Rear passengers not only get their own air vents, but drinks 
holders fitted in the centre rear seat squab.<br>
<br>
The fascia's ergonomics are impressive, the controls neatly laid, out and the 
steering wheel studded with audio and cruise control buttons. The boot is broad 
but not very high and the rear seats don't fold in the basic model that OER test 
drove. The Rover 75 C3 offers extras such as leather upholstery, 16&quot; alloy 
wheels, 8 airbags, wooden steering wheel and a fancy 6-CD changer but the C1 
version still comes with a well-stocked pamper and safety kit which includes 
cup-holders, a radio/cassette player, cruise control, four crash bags and seat 
belt pre-tensioners. <br>
<br>
The seats are a little like your favourite chair back home, you just don't want 
to get out. In true British fashion, the footwells are tight for feet larger 
than size 10 and rear seat headroom is marginal for average size men and women. 
To say the car is quiet would be an understatement. You have to look at the rev 
counter, just to make sure the car is ticking over. <br>
<br>
<b>Performance </b><br>
The car behaved itself admirably, be it driven with a purpose or as one would on 
a Friday. My first impression was that the five-speed self-shifter might not be 
doing justice to the generous 180 BHP output of the smooth 2.5-litre V6. The 
engine is sweet and appealing in the way it growls under load, but the 
transmission hunts around the gears. It doesn't provide much low-down torque 
either. <br>
<br>
A sliding 'sport-mode' button, hidden behind the selector lever, partly 
addresses the issue of poor low-down power, but makes the gearbox hyperactive. 
Shifts can also be slightly coarse under load. Whether in Sport or Economy mode, 
I felt the 1500-kg beast slightly slow to kick down, a fact not helped by the 
somewhat heavy throttle. However, the car's 8.9-seconds 0-60mph figure put to 
rest any such doubts that arose due to my non-believing fourth estate mindset.
<br>
<br>
In fact, once the 75 attains momentum, you can appreciate the velvety grunt; the 
Rover is much more content with a long stretch of freeway spooling out ahead of 
it. It cruises with impressive grace, more than adequate overtaking poke and 
terrific bump-absorption.<br>
<br>
It filters out rippled roads without resorting to overly soft springs and a 
rock-a-bye-baby ride and it's not prone to scraping its belly on bumpy roads or 
speed humps. The car is also very good at suppressing noise and purrs serenely 
at high speed. Road-holding characteristics are clearly tilted towards safety 
over driving thrills. <br>
<br>
It asks for a more demure driving style and, to be fair, very few drivers will 
punish it as hard as we did. However, if you have to brake hard in the middle of 
a curve or do a sudden avoidance manoeuvre, the Rover will take such drama in 
its stride. Jerk the steering wheel hard and the car will merely drift into 
benign understeer. <br>
<br>
The cruise control, while not impossible to use, does require a thorough study 
of the owner's manual. The 75 has a solid, planted-to-the-road feel with 
excellent directional stability and the anti-lock brakes, under-steer and slow 
steering rack make it utterly forgiving. <br>
<br>
<b>Safety </b><br>
From a car of this stature, one expects a comprehensive list of safety features, 
and one finds them, including side impact beams, head restraints and 
pre-tensioning three-point seat belts for all five seats; up to 8 front and side 
crash bags (built into the seat bolsters) are standard for driver and front 
passenger. The Rover 75 offers high standards of crash impact protection, with 
particular attention naturally being paid to the impact absorption 
characteristics of the rear end structure. <br>
<br>
Even the window glass is 4 mm thick instead of the usual 3 mm thickness - 
besides better safety, that also offers better sound insulation and a 
whisper-quiet ride. The new Rover 75 comes equipped with all the mod cons, 
starting with electric windows, electrically adjustable exterior mirrors, 
central locking with remote control. The remote also offers the choice of 
opening either just the driver's door or all four; a big safety factor. <br>
<br>
<b>The finishing line </b><br>
The Rover 75 is unmistakably British, classically stylish with a truly elegant 
and almost sinfully sumptuous interior. The ride is relaxed and superbly 
comfortable, if a little soft at higher speeds. Generally speaking, Rover have a 
winner in the 75. Some of the inner chrome trim doesn't fit as well as it 
should, but that is the only snag I could find. <br>
<br>
At first, I thought the car was being slow to respond -- then realised that, due 
to its quiet nature, what I thought was about 75 km/h was in actual fact 120 
km/h and I had to slow down and not drive it so hard. It's a car that is well 
designed, well built, drives beautifully and oozes class -- a regal, comfortable 
family exec with loads of space and a posh image. The Rover 75 has all the stuff 
to tempt status-conscious Audi and BMW punters to take a test drive. </font></p>

