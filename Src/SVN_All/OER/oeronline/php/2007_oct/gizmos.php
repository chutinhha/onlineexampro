<img border="0" src="gizmos.jpg" align="right" hspace="10" vspace="4" width="200" height="449">Are you looking for a mobile phone specifically designed to meet your 
entertainment, music and gaming needs? Then look no more. Nokia has introduced 
four new models (N81, N95 8GB, Nokia 5310 and Nokia 5310 Xpress Music) under its 
umbrella, each carrying the three nifty features embedded into them. <br>
<br>
The Nokia N81 and Nokia 81 8GB are simple and entertainment concentrated mobile 
multimedia computer phones. Both are sleek in design, with a polished surface 
and keys that lighten up when activated. In addition, they both have a 3.5 mm 
connector for headphone use along with 3G and WLAN connectivity, allowing fast 
download of multimedia content via the information superhighway (the internet). 
The phone allows you to search, buy and play music and games purchased directly 
from the Nokia Music Store and N-Gage games service.<br>
<br>
N95 series of the Nokia just got bigger with the addition of massive 8GB memory. 
Moreover, the phone also features further enhancements, such as a 5 megapixel 
camera with Carl Zeiss optics, built-in A-GPS, WLAN, HSDPA, innovative 2-way 
slide and new luminous 2.8” QVGA display – all allowing you to watch videos, 
browse the internet and view maps easily via the screen. With all this features, 
the Nokia N95 8GB is considered the world’s greatest multimedia computer as of 
date.<br>
<br>
Nokia 5310 and Nokia 5610 XpressMusic handsets are compatible and music player 
ready with the recently launched Nokia Music Store, allowing the user to 
download music and/or selection from your PC. The Nokia 5310 XpressMusic is 
small in size but BIG on music. Just 9.9mm thick and weighing 71 grams, the 
phone can easily fit in a pocket or purse as well. The phone has a 2-megapixel 
camera and a bright 2-inch QVGA screen with up to 16 million colors. Also, the 
Nokia 5610 offers crystal clear sound along with an embedded high-quality 3.2 
megapixel camera, featuring autofocus and dual LED flash ensuring good quality 
pictures. Unlike Nokia 5310, the 5610 is 3G technology enabled allowing faster 
downloads, internet browsing and video calls.<br>
<br>
If you desire to download and listen to music, play games and watch videos 
wherever you go, then check out these Nokia entertainment ready gadgets, as they 
may be the answer to your mobile wants and needs. <br>
