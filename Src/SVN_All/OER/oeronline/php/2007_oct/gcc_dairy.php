        becomes Zain</b><br>
        <img border="0" src="gcc4.jpg" width="214" height="134" align="right" hspace="6" vspace="4">Kuwait’s telecom giant MTC Group has taken up a new brand name Zain as 
        part of its bid to climb into the top 10 of global telecom operators, 
        officials said. “The move will bring all the company operations in 21 
        countries under one brand name. It is a part of our strategy to become 
        one of the top 10 global companies,” the CEO and general manager for 
        Kuwait, Barrak al-Sabeeh, said. The company’s operations in Kuwait, 
        Jordan, Bahrain and Sudan have immediately come under the new name, 
        while its operations in 14 African countries, Iraq and Lebanon will 
        change later. MTC in Saudi Arabia, slated to launch in early 2008, will 
        operate as Zain. The company has been awarded the third mobile licence 
        in Saudi Arabia. Sabeeh said that Zain headquarters would shortly be 
        based in Dubai or Bahrain or in the Dutch city of Amsterdam. He said the 
        company would continue to expand globally by chasing every investment 
        opportunity. Currently, Zain is bidding for licences in Qatar and 
        Algeria. The company’s customer base increased 40 per cent to 32.15 
        million by June 30, compared to 22.9 million subscribers a year ago.<br>
