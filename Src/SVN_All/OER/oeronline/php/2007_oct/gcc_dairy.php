		</font><b><font size="4" face="Verdana">Asset management on the rise</font></b><font size="1" face="Verdana"><br>
        <br>
        <b>Growing numbers of high net worth individuals, gradual economic reform 
        and the deepening of capital markets are encouraging many western 
        financial services firms to set up or expand asset management operations 
        in the Middle East, says the latest EIU report</b><br>
        <br>
        <img border="0" src="gcc.jpg" align="left" hspace="6" vspace="4">According to a recent survey by the Economist Intelligence Unit (EIU), 
        more than 80 per cent of respondents say that they expect to increase 
        their investments in the region over the next three years. A number of 
        factors, however, continue to deter potential investors, including 
        concerns about the regulatory environment, a lack of transparency and 
        political instability in the region.<br>
        <br>
        The survey forms the basis of Asset Management in the Middle East: The 
        prospects for global financial institutions, a newly released EIU report 
        sponsored by the Qatar Financial Centre (QFC) Authority. The report 
        looks at the business environment for asset management in the Middle 
        East, examines the region’s developing investor culture and explores 
        some of the products and services that are likely to attract attention 
        as the sector develops.<br>
        <br>
        “There is a strong appetite among global financial institutions to 
        invest in the asset management sector in the Middle East,” says Rob 
        Mitchell, editor of the report.<br>
        <br>
        “High levels of wealth are fuelling strong demand for private banking 
        and wealth management services, while greater maturity among investors 
        and a more robust financial infrastructure are encouraging the 
        development of the retail investment sector. But despite these 
        opportunities, a number of problems persist that are deterring further 
        investment, including a lack of transparency, concerns about the 
        regulatory environment and difficulties recruiting skilled finance 
        professionals in the region,” he adds. <br>
        <br>
        The conclusions of the report are based on a survey of 180 senior 
        executives from financial services firms conducted by EIU and a series 
        of in-depth interviews with representatives from companies that are 
        already investing in the region.<br>
        <br>
        <b>Key findings</b><br>
        Private banking is seen as the strongest investment opportunity. 
        Financial institutions that offer private banking and wealth management 
        services see the Middle East as an increasingly important market, and 
        many are recruiting or relocating client relationship managers to serve 
        the region. The growing ranks of high net worth individuals, coupled 
        with developments in the capital markets in the region, are increasing 
        demand for more sophisticated products, including derivatives, hedge 
        funds and exchange traded funds. Other important investment 
        opportunities cited by respondents include private equity and retail 
        investment.<br>
        <br>
        Islamic finance is becoming an important component of the asset 
        management offering. Both survey respondents and interviewees are 
        confident that the growth of the Islamic finance sector will continue. 
        As of now, it has not been particularly strong in many areas of asset 
        and fund management, but this is likely to change. Almost 60 per cent of 
        respondents say that they expect a substantial increase in demand for 
        Islamic finance products over the next three years, and a growing 
        proportion of respondents say that they expect at least some of their 
        products to be Shariah-compliant.<br>
        <br>
        Strategic alliances are seen as the most favoured entry or expansion 
        strategy. Entry or expansion strategies into the Middle East depend on 
        individual market conditions and the regulatory environment of the 
        specific country but, in general, respondents favour strategic alliances 
        and joint ventures. With the provision of banking licences becoming more 
        commonplace, it is likely that more and more foreign banks will take the 
        branch licence or representative office approach.<br>
        <br>
        A number of issues continue to deter investment in the region. Despite 
        progress in areas such as the regulatory and business environment, 
        respondents point to a number of deterrents to further investment in the 
        region. Concerns about the geopolitical environment remain high on the 
        agenda for many in the sector, with 71 per cent citing this as 
        significant. A lack of transparency and poor standards of regulatory 
        regime are also raised as an issue, with 65 per cent citing these as 
        deterrents to investment.<br>
