        <b>Call to change weekly holidays </b><br>
        Saudi Arabia will suffer heavy economic losses by continuing to follow 
        the Thursday-Friday weekend routine, according to businessmen who have 
        renewed their call to change weekly holidays to Friday and Saturday. The 
        new call came after Kuwait, also a member of the Gulf Cooperation 
        Council (GCC), introduced the Friday-Saturday weekend despite objections 
        from certain sections. Among the six GCC countries, four – Bahrain, 
        Qatar, Kuwait and the United Arab Emirates – have now switched over to 
        the new system with only Saudi Arabia and Oman following the traditional 
        Thursday-Friday weekend. Economists say that the continuation of 
        Thursday-Friday weekend will put Saudi Arabia a month behind other 
        countries, including some GCC states, in a year. “By sticking to the old 
        system Saudi Arabia will continue to suffer heavy economic losses and 
        will be isolated from the rest of the world,” one businessman said. 
        Adnan Al Naeem, secretary-general of the Eastern Province Chamber of 
        Commerce and Industry, said that the difference in weekends causes heavy 
        losses to businesses in the Kingdom.<br>
