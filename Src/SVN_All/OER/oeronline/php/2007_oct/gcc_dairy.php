        <b>DubaiSat-1 space project in 2008</b><br>
        <img border="0" src="gcc6.jpg" width="118" height="169" align="right" hspace="6" vspace="4">Emirates Institution for Advanced Science and Technology (EIAST) has 
        unveiled the various phases in the execution of its first satellite 
        project, DubaiSat-1. To be launched in 2008, DubaiSat-1 will support the 
        infrastructural development by providing information that is central for 
        the decision-making process affecting the urban and rural planning as 
        well as transport, utilities, and mapping, in addition to environmental 
        applications such as monitoring pollution and detecting oil spills. 
        DubaiSat-1 will also help manage natural disasters and promote research 
        and development, space science and other scientific disciplines. This 
        pioneering satellite project consists of three phases over a three-year 
        period. The first phase started in 2006 and saw the institution develop 
        the strategies, budget, administrative structure, and the necessary 
        staff. The second phase, covering this year, includes the design and 
        development of the prototypes for the equipment and tools of the 
        satellite, and mounting them on the satellite on an experimental basis, 
        and contracting the launching company, Moscow-based International Space 
        Company (ISC) Kosmotras. The last phase, that begins next year, will 
        cover the final testing of the satellite with all its equipment and 
        specifications in actual space conditions and its performance as an 
        integrated system. This will be followed by the actual launch of the 
        satellite in the space in 2008. <br>
