        <b>Lighting up the Grand Prix</b><br>
        <img border="0" src="gcc16.jpg" width="250" height="140" align="left" hspace="6" vspace="4">The Qatar Grand Prix next year will be the first MotoGP race to be held 
        at night, in what is believed to be the biggest lighting project in the 
