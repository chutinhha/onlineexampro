        <b>Mobile market to double by 2010</b><br>
        <img border="0" src="gcc1.jpg" width="214" height="172" align="right" hspace="6" vspace="4">Nokia’s President and CEO Olli-Pekka Kallasvuo said that strong industry 
        growth in the Middle East and Africa region should continue, with mobile 
        device volumes expected to more than double from 2006 to 2010. “We 
        estimate that the Middle East and Africa region may have the highest 
        growth rate in industry device volumes over the next three to four 
        years,” Kallasvuo said during the official opening of Nokia’s new 
        regional office in Dubai.<br>
        <br>
        ”We have forecasted a global market of 4 billion subscriptions in 2010, 
        up from the current level of just over 3 billion. And we expect that 
        nearly a quarter of the increase in subscriptions will come from this 
        region,” he said. <br>
        <br>
        “We plan to strengthen our operations by reaching further into rural 
        communities to bring mobile communications to more people,” he added. 
        Nokia expects the region’s industry device volumes to more than double 
        by 2010 from the estimated over 100 million devices sold in 2006 <br>
