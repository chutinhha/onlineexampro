        <b>Aviation on demand</b><br>
        <img border="0" src="gcc10.jpg" width="250" height="179" align="left" hspace="6" vspace="4">Royal Jet, the GCC business aviation pioneer and international luxury 
        executive flight services provider, has launched a cost-effective 
        Learjet service in a tactical move to broaden private charter appeal 
        within the region. Using its newly acquired, twin-engined Learjet 35s, 
        the Abu Dhabi-headquartered operator has lowered chartering rates for 
        the six-seater aircraft by around 20 per cent of prevailing market 
        rates. Royal Jet’s Learjet 35s can be chartered for US$2,850 an hour. 
        “This is a tactical move to demonstrate the affordability of private 
        charter when, for instance, point-to-point, day return private travel 
        from the UAE to Kuwait can be achieved for US$10,000 for six people,” 
        said Shane O’Hare, President and CEO, Royal Jet. “This aircraft is one 
        of the fastest in its class. It is a proven business tool in it that 
        enables time-pressed executives to undertake business meetings 
        throughout the region at their own schedules. It would, for instance, be 
        perfectly feasible to fly to meetings, say from Abu Dhabi to Manama, 
        Riyadh or Kuwait and returning back in a single day, something which 
        cannot be achieved with scheduled services,” explained O’Hare. “Clients 
        also have the advantage of checking in at VIP terminals avoiding airport 
        and aircraft procedures.”</font></p>
