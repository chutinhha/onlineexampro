<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
<b>BODYSPEAK</b><br>
<br>
</font><b><font face="Verdana" size="4">Wellness at work<br>
</font></b><font face="Verdana" size="1">
<br>
<img border="0" src="health.jpg" width="250" height="177" align="left" hspace="6" vspace="4">For those who are fitness freaks and those who aspire to be one, the opening of 
a new modern wellness facility – bodySpeak-equipped with state-of-the-art 
fitness equipment right in the heart of Muscat city is a boon. Launched by 
Khimji Ramdas Special Projects Division (KRSPD), in the KR annexe building in 
Muttrah, the interactive gymnasium-cum-showroom offers the latest fitness 
equipment from the US-based Cybex International to give a truly first class 
experience to de-stress and revitalise one’s energy. <br>
<br>
Spread over 3,000 sq ft, the contemporary and vibrant bodySpeak is equipped with 
bio-mechanically designed sophisticated exercise equipment: cardio machines, 
free weight stations, circuit machines, dumbbells, sauna and steam bath, along 
with a revitalising juice bar. The friendly lighting and piped music spilling in 
through submerged Bose speaker systems are bound to make you feel comfortable in 
a perfect ambience. <br>
<img border="0" src="health1.jpg" align="right" hspace="6" vspace="3"><br>
BodySpeak follows a fresh and unique approach to marketing through an 
interactive and dynamic showcase facility that serves a dual purpose. The 
facility supported with professional trainers is available to KR staff and 
select clientele at an affordable cost. It focuses on quality and specialises in 
providing a fitness regimen coupled with health advice as part of a Fitness and 
Lifestyle Improvement Programme (FLIP). Furthermore, bodySpeak also serves as an 
interactive display, showcasing the Cybex brand and KRSPD Fitness Unit’s 
developmental and maintenance support services. As an extension, the facility 
will also serve as a training ground for fitness experts. <br>
<br>
How did it all happen? The Fitness Unit of KRSPD conducted 
<img border="0" src="health2.jpg" width="250" height="187" align="left" hspace="6" vspace="4">a study to correlate 
the market expectations vis-à-vis availability. The study revealed that there 
was good potential for the market to go a step ahead than just selling 
equipment. Following a close assessment of the market scenario and customer 
requirements, it emerged that it is not only important to provide quality 
fitness equipment with good technical support but also skilled staff to teach 
and manage fitness. <br>
<br>
The findings led to the creation of bodySpeak. Karem Shaik, Business Manager, 
says: “Our Fitness Unit focuses on providing customised solutions and 
conceptualisation of wellness facilities to corporates, government and 
educational institutions, hospitality sector and commercial operators on a 
turnkey basis ‘from thought to finish’ and manages them.”
