<p align="justify" style="margin-left: 10px; margin-right: 10px"><font face="Verdana" size="1">
<br>
</font><b><font face="Verdana" size="4">Stop Trying to Measure PR</font></b><font face="Verdana" size="1"><br>
<b>Common PR measurement tools have no link whatsoever with sales and 
profitability, argues Nick Wreden<br>
</b><br>
Although public relations (PR) measurement is almost always an exercise in 
futility or smoke-and-mirrors, it is important to note that PR has substantial 
value. Even in an age where customers define brands based on their economic, 
experiential or emotional value, PR plays a critically important role in shaping 
attitudes and driving sales. PR has a much greater impact on a brand than 
advertising, and plays a vital role in internal branding. As a result, the 
smartest branding tactic for almost all companies is to immediately increase 
their PR budget.<br>
<br>
Every PR professional knows and understands this. CEOs who have squirmed under 
the harsh light of media scrutiny also know this. But everyone else feels the 
urge to place PR in the same category as stationery and force PR departments to 
justify or validate their expenditures.<br>
<br>
And it is because of this unthinking pressure that PR departments and agencies 
spend substantial sums “measuring” results. Starting with clip counting (“PR by 
the pound”), PR results are now increasingly evaluated through AVE (advertising 
value equivalency), which links the value of exposure to advertising rates, with 
some multiplier ranging from 1.1 to 8 thrown in because the mentions have more 
credibility than advertising.<br>
<br>
But, AVE has also become the industry’s favourite whipping boy for three 
reasons. First, there is no research documenting a reliable “credibility 
multiplier.” Second, appearance does not translate into action or even 
absorption by the target audience. And, finally, results can be easily 
manipulated, with “credit” for an article with just a mention. With AVE, every 
agency can “prove” big-bang results from sending out a press release.<br>
<br>
In fact, a random sample of 4,200 PR professionals revealed that about one-third 
still use AVE to justify results. Sometimes, the rationalisation for using AVE 
is almost laughable. “Its just easier to go with a straight number,” says the 
co-founder of Excom Communications in Los Angeles. Even more telling was the 
headline in a report about AVE: “Like it or not, PR pros use ad value 
equivalency to appease dollars-and-executives.” Appease? Is that the driver 
behind PR measurements?<br>
<br>
Another favourite PR measurement tool is before-and-after surveys. Sears Roebuck 
&amp; Co. landed a positive story on The Oprah Winfrey Show about donating US$20,000 
worth of goods to needy families for Christmas. An article about the effort 
said, “The respondents who said they agreed with the statement ‘Sears is a 
quality company’ increased from 58 per cent to 65 per cent.” <br>
<br>
<b>Hard questions</b><br>
Kudos to Sears for its generosity, and no doubt there are brand benefits from 
having 7 per cent more people think that you are a “quality company.” But hard 
questions remain. What was the actual, measurable impact on sales and 
profitability? If Sears cannot answer that question, how can it determine the 
ROI from its US$20,000 investment?<br>
<br>
The current hot metric is “share of voice/discussion.” This metric is 
essentially based on the premise that the more the media is discussing your 
company, or raising an issue your company has a stake in, the more target 
markets will buy your offering or believe your side.<br>
<br>
That’s a reasonable – although unproven – hypothesis, but measurement of 
share-of-voice is closer to voodoo than science. Take a large handful of 
corporate and competitive mentions, tag them with a positive, negative or 
neutral value, calculate a “net favourable media value” before eventually 
emerging with a final “share of discussion”.<br>
<br>
Again, with no link to profitability and sales, nothing is proved from these 
common methods other than the fact that you got your name in the paper. 
Additionally, with the growth of blogs and other online media, it has become 
even harder to prove a link between PR outputs and sales outcomes.<br>
<br>
So how can PR justify its expenditures? The best way is to include PR as part of 
a comprehensive lead management system. Lead management systems can track leads 
from source to sale. This allows companies to not only determine the best 
sources of leads but also ensure that leads do not fall between the cracks.<br>
<br>
According to the Yankee Group, as many as 80 per cent of all leads languish 
without follow-up or remarketing. Following up on these lost leads can increase 
sales by 10-20 per cent, says a survey by the CMO Council and the BPM Forum.<br>
<br>
McGraw-Hill has even scarier data: 18 per cent of inquiries receive no 
information; 43 per cent receive the information too late; 59 per cent receive 
the wrong information; and 90 per cent of all leads are never followed up.<br>
<br>
So stop throwing money at so-called PR “measurement” systems. You might as well 
figure out how many angels can dance on the head of a pin. Instead, put those 
resources into a lead management system, and link that system to sales and 
profitability.<br>
<br>
<i>Nick Wreden is the CEO of the leading international brand consultancy, 
FusionBrand, and the author of ‘ProfitBrand: How to Increase the Profitability, 
Accountability and Sustainability of Brands’. </i></font></p>
