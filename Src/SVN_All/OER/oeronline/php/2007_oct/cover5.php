<p align="justify" style="margin-left: 10px; margin-right: 10px"><font face="Verdana" size="1">
<br>
</font><font face="Verdana" size="4">Advertorials<br>
News or Sleaze? The Debate Continues</font><font face="Verdana" size="1"><br>
<br>
<b>In print, advertorial content is often clearly indicated. But, says Brian 
Salter, the emergence of ‘new media’ is changing all this as even newspapers 
online are integrating more and more advertorials<br>
</b>
<br>
What do you call a human discipline which is firmly rooted in the principle that 
people act on their own perception of the facts, then creates, changes or 
reinforces public opinion by reaching out, persuading and moving-to-action those 
very people whose behaviour affects an organisation? <br>
<br>
This is the fundamental premise of public relations (PR) since it can alter 
perceptions and hence the behaviour of certain groups of people important to the 
success of a business.</font></p>
<p align="center" style="margin-left: 10px; margin-right: 10px">
<img border="0" src="cover10.jpg" width="350" height="228"></p>
<p align="justify" style="margin-left: 10px; margin-right: 10px">
<font face="Verdana" size="1">PR is all about reputation – the result of what you do, what you say and what 
others say about you. And PR practice aims to earn understanding and support, 
and thereby influence opinion and behaviour. It does this through education and 
persuasion. Education seeks to change the way someone perceives an event and 
persuasion seeks to change how they interpret the facts they have perceived.<br>
<br>
There is broad agreement that people really do act on their perception of the 
facts, and that how they react to those facts actually affects their behaviour. 
So, it follows that a sound PR strategy combined with effective communications 
tactics can directly affect the bottomline of an organization. Businesses of all 
sizes spend significant sums of money attracting attention to their services or 
products. In today’s world of mass communications, the public is overwhelmed by 
advertising messages. Yet, publicity in the form of independent print, broadcast 
or online articles about a company or its products and services can attract the 
attention of potential customers in a way that one more advertisement simply 
cannot.<br>
<br>
<b>Lending credibility</b><br>
A major tool of PR is to use the media as conduits for placing articles about a 
business in order to gain credibility in the eyes of the public. People often 
think about using a product or service for the first time after reading an 
article about it in a newspaper or magazine or on a website.<br>
<br>
Much can be said as to which is better: advertising or PR. An advertising 
executive will say that advertisements offer immediate impact whereas a PR man 
will say that public relations is much more strategic.<br>
<br>
Conducting interviews and sending out news releases does not guarantee coverage 
but it increases your chances considerably. And if you are quoted or profiled in 
a given article, the chances are that the publication’s subscribers, who are 
ultimately your current or potential clients, will read it.<br>
<br>
It is widely known that people give a lot more credibility to good editorial 
content than to paid advertisements. After all, anyone can claim that their own 
product is the best. But editorial content suggests that someone else has 
endorsed your product or service.<br>
<br>
An advertorial is a half-way house – an advertisement written in the form of an 
objective opinion editorial and presented in a printed publication – usually 
designed to look like a legitimate and independent news story. The term 
“advertorial” is a contraction of “advertisement” and “editorial”.<br>
<br>
Media businesses depend wholly or in part on advertising revenue for 
publication; and journals will attract advertisers when their readers are 
attracted by the content. But the dichotomy is that advertisers pay to ensure 
there is no critical view, which runs counter to their advertising aims. 
Therefore, an advertiser has oversight of the copy, final approval or some 
measure of control to ensure the words and images are tied to his advertising 
purpose.<br>
<br>
</font><b><font face="Verdana" size="1">Special promotion</font></b><font face="Verdana" size="1"><br>
Most publications will not accept advertisements that look exactly like stories 
from the newspaper or magazine they are appearing in. The differences may be 
subtle, and disclaimers – such as the word ‘advertisement’ – may or may not 
appear. Sometimes euphemisms describing the advertorial as a ‘special 
promotional feature’, or the like, are used. <br>
<br>
Advertorials commonly advertise new products or techniques. The tone is usually 
closer to that of a press release than of an objective news story since 
advertisers will obviously not spend money to describe the flaws in their 
products!<br>
<br>
A related practice is the creation of material that looks like traditional media 
(for instance, a newspaper or magazine), which is in fact created by a company 
to market its products. One familiar example is an in-flight magazine, which 
usually features reports about travel destinations to which an airline flies. 
Some editors expect readers themselves to spot the commercial interests that lie 
behind a supposedly disinterested contributor.<br>
<br>
But many will argue that advertorials betray the trust of the journal’s 
readership. “Advertorials should have no place in magazines with quality 
editorial, the critical feature to ensure survival in today’s market,” says 
Michael Heseltine, the Chairman of the UK’s Haymarket Group. And The Guardian’s 
Simon Jenkins goes further, saying that “to present PR as journalism is close to 
sleaze.”<br>
<br>
So what about the readers? If they can’t trust their magazine, the integrity of 
its advice and its independent voice, then why should they remain loyal? Before 
being pushed into compromising positions by their publishers, editors should 
remember the old adage that people who feel cheated are not receptive to 
advertising messages. Magazines that trade credibility and reader loyalty are as 
good as selling their family silver. <br>
<br>
<b>No one answer</b><br>
Brands need magazines with readers who trust what they read, not those who are 
wary, jaded and cynical. It’s a difficult question and not something that 
necessarily has a right or a wrong answer. On Action Qatar’s PR training 
courses, we spent a fair amount of time discussing the pros and cons of 
advertorials. Some are passionately in favour of them whilst others take the 
line espoused by Simon Jenkins. I remember many years ago going to a job 
interview for a communications position with a blue chip client. He opened the 
proceedings with a classic question that I’m sure I’ll never forget: “Tell me, 
Mr Salter; do you have any morals?”!<br>
<br>
Yet, advertorial content is both common and legal, albeit that many countries 
now insist that it must be described as such, typically with a caption that 
might read ‘Advertisement’ or ‘Special Promotion’. If there is no indication 
that the advertorial is different from normal editorial, there may be good cause 
for complaint.<br>
<br>
However, the emergence of ‘new media’ is changing all this,. Traditional print 
media attracts advertisers through unbiased reporting. Advertisers interrupt the 
information with classified ads, display ads, and full-page advertising. 
Consumers buy the products if they stop reading the information and respond to 
the ad. Online, a different model is emerging. You still have unbiased news 
reporting, but even newspapers online are integrating more and more 
advertorials.<br>
<br>
It’s becoming a hybrid model: like infomercials, this form of advertising works 
because it mixes information with advertising. Online advertisers reach 
audiences through endorsement and recommendation. Placing an ad to interrupt a 
web page, like a banner ad, simply does not yield performance in terms of sales 
or traffic. But if the article favourably recommends a product, and there is a 
hyperlink to the advertising message, then sales have been shown to increase. 
Businesses don’t buy space; they try to work into a favourable position within 
the web site. And this has implications for advertorials, as advertising and 
editorials start to merge. This grey area of advertising is fuelling much 
debate, but it is already being used by many web sites, including those of some 
of the most prestigious publishing houses. It is the new way of marketing on the 
internet.<br>
<br>
<b>Perceived value</b><br>
Advertorials will generally be placed within their own pages on the host web 
site. This has the advantage that the links to the site being promoted come 
directly from a contextually relevant page – unlike most advertising links, that 
are generally placed on pages that have lower contextual relevance. Online 
advertorial and editorial have a much higher perceived value to the consumer and 
therefore have significantly higher penetration and click through rates than 
online banner or textual adverts. For the advertisers, advertorials yield less, 
but better traffic to their product positioning. Those who click the hyperlinks 
are the very people who are genuinely interested in the product before they 
actually reach the advertising messages. <br>
<br>
So the advertorial is already changing the traditional face of advertising. In 
printed media, editorial space and writers – who in the main are unbiased – 
generate readers. And many readers generate advertising riyals as the 
advertisers chase potential receptors for their messages. But the print model of 
selling ad space does not work for most online sites. Selling advertising space 
on the internet is tough; even for those who do it well, there is always dead 
advertising space. But using advertorials to generate interest has already 
proved its worth.<br>
<br>
<i>Brian Salter is a senior consultant working for Action Global Communications 
Group. 
</i>
