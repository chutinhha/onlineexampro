<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
</font><b><font face="Verdana" size="4">
Think out of the Pyramid</font></b><font face="Verdana" size="1"><br>
<b>Higher levels of education and access to information mean that structures 
often negatively affect people’s behaviour and motivation, and consequently 
organisational performance, writes Robert Hooijberg</b><br>
<br>
To remain successful, modern organisations need to tap into the knowledge and 
skills of their people, of course. However, the traditional pyramidal management 
structure can hinder rather than facilitate that goal. New approaches are needed 
that foster alignment, insight, cooperation and initiative among an 
organisation’s entire workforce.<br>
<br>
In a chapter from the recently published book “Being There Even When You Are 
Not: Leading Through Strategy, Structure and Systems,” Robert Hooijberg and Paul 
V. Broeckx talk about getting the most from your workforce.<br>
<br>
<b>Limitations of a pyramidal structure</b><br>
Pyramidal structures were originally designed to manage poorly educated, 
ill-informed people who needed supervisors to tell them what to do and how to do 
it. But these days, high levels of education and access to information mean that 
such structures often negatively affect people’s behaviour and motivation – and 
consequently organisational performance. Here are the worst limitations of the 
traditional pyramidal structure:<br>
<br>
<b>Vertical career progression</b>. The hierarchical ladder frequently becomes a system 
in itself. People may choose a particular career path simply to enter top-level 
management, even if it does not match their profile or skill set.<br>
<br>
<b>Command communication</b>. A top-down command mode, where orders are swiftly 
executed without being questioned, has long been accepted as the most efficient 
way to produce results. But by not discussing the expected results, alignment is 
achieved between the superior’s expectations and the action instead of between 
the action and the expected results. This communication style also assumes that 
people at lower ranks cannot contribute to strategies and objectives.<br>
<br>
<b>Company experience</b>. While experience is obviously valuable, length of experience 
and seniority have often been confused. The cost of promoting people into 
leadership roles mainly because they have been with the company for years has 
become unaffordable today. In addition, these days experience (like knowledge) 
can rapidly become obsolete and a killer of creativity and initiative.<br>
<br>
<b>Internal competition</b>. The traditional managerial principle puts employees in a 
competing mode, which can restrict and even discourage knowledge sharing and 
cooperation.<br>
<br>
<b>Silo thinking</b>. The traditional managerial role of centralising information and 
being the sole point of communication between the team and top management is 
still embedded in the mentality of many managers today. This, together with 
reporting lines to one superior and the tendency of internal competition to 
create strong “allegiance” to the superior, automatically creates silos.<br>
<br>
<b>Obedience orientation</b>. With the quality of task execution defined by the 
superior, it is more important to be obedient and conform to the superior’s 
expectations than to display initiative and concentrate on results.<br>
<br>
<b>Delegation of tasks rather than authority and responsibility</b>. Traditionally, the 
manager retains decision-making authority and is often the “correcting” manager 
as well as the only judge of performance quality. But in trying to maintain 
their superiority, most managers tend to stifle others’ self-confidence, leading 
to lower motivation and efficiency.<br>
<br>
<b>Building on human potential<br>
</b>These limitations block four key factors that modern organisations need from 
their people in order to take full advantage of their knowledge and skills-and 
thus ensure long-term profitability and sustainable company development. These 
are: </font>
<ul>
  <li>
  <p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><b>Alignment</b>: A clear sense of the expected results and company values, with 
everyone convinced and engaged</font></li>
  <li>
  <p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><b>Insight</b>: The process of transforming experience into action</font></li>
  <li>
  <p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><b>Cooperation</b>: The genuine sharing of insights and knowledge</font></li>
  <li>
  <p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><b>Initiative</b>: Improved efficiency through motivated, self-confident employees</font></li>
</ul>
<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1">When these four factors come together, silos and the silo mentality disappear. 
This alone is worth the effort and will unlock additional efficiency and 
effectiveness. There is also improved alignment with results-exactly what 
companies wish to achieve.<br><br><b>New management structures</b><br>How can companies break out of the pyramid structure and achieve alignment, 
insight, cooperation and initiative? We believe the answer is through flatter, 
more flexible structures where managers are more like “hubs” that connect people 
and combine skills, managing through a network rather than a traditional 
hierarchy. High-quality processes are needed for decision-making and debate, as 
are new ways of assessing people to give priority to insight over experience.<br>
<br>Nestle’s recent evolution from a pyramidal structure to a network organisation 
provides proof of the value of implementing such an approach. The company 
achieved this through its “Nestle on the Move” programme, which has five major 
parts:</font></p>
<ol style="font-family: Verdana; font-size: 8pt">
  <li>
  <p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><b>Implementing flat and flexible structures</b>: An in-depth examination of corporate 
structure and functions was undertaken, and the number of hierarchical levels 
was reduced.</font></li>
  <li>
  <p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><b>Inspiring management</b>: Programmes were implemented to improve the leadership 
skills of managers, starting at the top level, as well as to reverse their role 
from “passive judge” to “committed developer,” and so make them responsible for 
the development of their people.</font></li>
  <li>
  <p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><b>Long-term development</b>: With fewer hierarchical levels, fewer promotion levels 
are now available. Interregional and interfunctional moves were therefore 
enhanced to stimulate both personal development and organisational learning, and 
new roles were created that cut across traditional career paths.</font></li>
  <li>
  <p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><b>Dynamic compensation</b>: Horizontal remuneration models were developed that allowed 
increased remuneration even without promotion.</font></li>
  <li>
  <p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><b>Lifelong learning</b>: Nestle has always held a strong belief in the need for 
continuous learning, both through internal and external programmes.</font></li>
</ol>
<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1">“Nestle on the Move” has made an important contribution to company results in 
terms of drive and excitement. Leadership skills have demonstrably improved, and 
the new compensation model has improved the company’s competitiveness at all 
levels. Nestle provides a clear example of how a company should manage an 
emancipated workforce, and demonstrates, in a concrete manner, that people are 
the most important asset.<br><br><i>(Professor Robert Hooijberg is a professor at the International Institute for 
Management Development. Paul Broeckx is director of Nestle’s Corporate Human 
Resources Division.)<br><br>From The International Institute for Management Development c. 2007 
International Institute for Management Development (Distributed by The New York 
Times Syndicate.) </i><br><i>. </i></font></p>
