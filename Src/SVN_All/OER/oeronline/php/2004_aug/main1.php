                    <p align="justify">
                    <font face="Verdana" size="2" color="#CC3333">
                    <b>Sultanate of Oman: </b>
            </font><font face="Verdana" size="1">
                    <br>
                    <b>Khareef Salalah Festival</b><br>
                    <br>
                    Although Oman's economy is geared by the petroleum sector, 
                    the country has realised that it must move away from a 
                    hydrocarbon dependency and is expanding its non-oil 
                    portfolios for income generation. The increasing number of 
                    tourists coming to the Sultanate is a good omen for the 
                    country, as it is targeting tourism to be one of the major 
                    revenue earners in the coming years. The country aims at a 3 
                    per cent contribution to GDP (RO600,000 or about $1.5 
                    billion according to 1999 figures) from tourism by 2020.<br>
                    <br>
                    <img border="0" src="im25.jpg" align="left" hspace="5" width="200" height="139">In 
                    this regard, the government is making all efforts to open 
                    Oman to the world, and special festivals like the Khareef 
                    Salalah and the Muscat Festival have become special annual 
                    events to help boost the tourism sector. <br>
                    <br>
                    Its beautiful scenery and cool climate make Salalah a place 
                    blessed with heavenly bliss, when scorching heat and high 
                    humidity engulfs the entire region. The best season in 
                    Salahah is from late June until the end of August. This is 
                    time when this area experiences cooling rains from the 
                    southwest monsoon winds. The Salalah Festival is well timed 
                    with the good season to increase the tourist flow into the 
                    country. The festival is usually held over a six-week period 
                    from mid-July until the end of August. The event promotes 
                    the Dhofar region and hosts a variety of cultural, sports, 
                    and entertainment activities. Offering an ideal place for 
                    families to enjoy their summer holidays, the Salalah Khareef 
                    Festival 2004 opened on July 15 and will last till August 
                    31. <br>
                    <br>
                    Started in 1995, the Khareef Salalah Festival has become 
                    popular among tourists from across the world, especially 
                    from within the GCC. The festival also attracts many 
                    tourists from within Oman. Over the years, it has grown into 
                    an international festival thanks to the long-standing 
                    efforts of the authorities to expand the existing tourism 
                    infrastructure in Salalah. <br>
                    <br>
                    This year, it is expected that a record number of visitors 
                    will visit Salalah during the festival season. A lot of 
                    effort has been put into marketing and promoting the event. 
                    The Dhofar Municipality's Festival Department is responsible 
                    for marketing, promoting and organising the Khareef 
                    Festival. Last year (2003), the Municipality actively 
                    participated at Discover Oman - Oman's first adventure and 
                    tourism exhibition held between September 25 and October 3, 
                    2003, at Oman International Exhibition Centre. <br>
                    <br>
                    By participating prominently at Discover Oman, the 
                    Department attracted huge numbers of potential domestic 
                    tourists, both Omanis and expatriates from Muscat and 
                    neighbouring Wilayats. During this event, a lot of 
                    information about Khareef Festival activities was given to 
                    visitors and they were told about various tourism and 
                    adventure destinations and facilities which the Khareef 
                    Salalah Festival has to offer. <br>
                    <br>
                    Besides the road shows to promote the Khareef Festival in 
                    Salalah which began as early as April 1999, the government 
                    has also carried out many international campaigns to market 
                    the event. &quot;We have worked very hard in the last five years 
                    to promote tourism during the Khareef season. Our campaign 
                    was not limited to the Gulf, but to other Arab countries, 
                    Europe and Japan. Our embassies and missions abroad have 
                    also put in a lot of efforts to market the country as a 
                    serious tourist destination,'' a Tourism Department official 
                    said. <br>
                    <br>
                    This year, on the promotion plan, Dhofar Municipality has 
                    included advertising on Oman TV and Arab satellite channels 
                    (a total of 1,200 advertisements), local and Arab press (170 
                    advertisements) and publishing of thousands of brochures. 
                    The Website of Dhofar Municipality also helps in promoting 
                    the festival as it keeps the surfers updated with the 
                    events. <br>
                    <br>
                    Most tour companies have witnessed a steady increase of the 
                    tourist flow to Salalah in the last five years. They believe 
                    that the Dhofar region will become a major income earner for 
                    the tourism industry. This year, they expect a larger 
                    tourist turnout thanks to a comprehensive marketing campaign 
                    carried out by the government. Many tourism information 
                    centres have also been set up to provide tourists with 
                    various services. These centres are located at Hareet border 
                    checkpoint, Salalah airport and at the Municipality 
                    Entertainment Centre at Ateen Plain. The tourism information 
                    centres provide visitors with guiding pamphlets, brochures 
                    and maps. <br>
                    <br>
                    To facilitate inflow of visitors, special arrangements are 
                    in place including visa relaxation, special promotional 
                    fares by Oman Air, and discounted packages by all hotels. 
                    Many new events and activities will feature in this year's 
                    festival. The opening ceremony was in the form of a 
                    carnival. The authorities thought it fit to launch a 
                    carnival and shopping festival this year to further enhance 
                    the Khareef attraction. <br>
                    <br>
                    A lot of religious and cultural programmes, theatrical shows 
                    and special programmes for families and kids have been 
                    organised. Participants from Oman, Gulf and Arab countries, 
                    including Saudi singer Mohammed Abdu, will perform during 
                    the festival. Besides the Khareef Salalah Festival, Muscat 
                    Festival, which is held during January, has succeeded in 
                    drawing visitors from the neighbouring countries. The 
                    festival offers a wide range of activities. Culture, 
                    education, sports, tourism and the arts are highlighted 
                    during the festival. <br>
                    <br>
                    Promoted under the motto 'Yes to Oman', Muscat Festival aims 
                    to bring Oman under limelight and has gained popularity with 
                    each passing year. The festival is organised by Muscat 
                    Municipality with the support of the private sector. The 
                    festival focuses on the traditional and modern aspects of 
                    Oman. <br>
                    <br>
                    A record number of visitors attended Muscat Festival 2004. 
                    The event boosted the tourism, business and economic sectors 
                    of Oman, earning rich dividends for each of these sectors. 
                    The number of visitors reached 1.5 million in 2003, and 
                    hotels achieved 100 per cent occupancy and the overall 
                    economy benefited.</font></p>

                    <p align="justify"><font size="2" color="#CC3333">
                    <font face="Verdana"><b>Kingdom of Saudi Arabia:</b></font></font><font face="Verdana" size="1">
                    <br>
                    <b>The Jeddah Ghair Festival</b><br>
                    <br>
                    The annual Jeddah 25 Festival opened on June 17, 2004. Known 
                    for its many entertaining, social, sports, educational, 
                    recreational, culture, arts, technology and scientific 
                    activities, the Jeddah Ghair has the best of what Jeddah has 
                    to offer. The event is a six-week-long extravaganza, lasting 
                    till July 30, 2004. <br>
                    <br>
                    <img border="0" src="im26.jpg" align="right" hspace="5" width="250" height="141">The 
                    Jeddah festival, one of the biggest events held in the 
                    Kingdom every year, has successfully boosted the tourism 
                    industry in the country. According to official figures, 
                    tourists who visited Jeddah during last year's summer 
                    festival spent more than SR880 million (RO90.23 million). 
                    The first Jeddah summer festival started in 1999, and even 
                    during that time, it attracted about 1.2 million visitors. 
                    Thereafter, the festival has successfully been able to rope 
                    in more tourists, which has increased capital flow into the 
                    country. In the year 2000, the Jeddah festival generated 
                    business worth SR1.769 billion (RO181.38 million). Of this, 
                    retail traders grabbed the lion's share of SR721 million 
                    (RO74 million), followed by recreation centres. <br>
                    <br>
                    According to a study conducted by a specialised office in 
                    cooperation with the research centre at the Jeddah Chamber 
                    of Commerce and Industry, foreign tourists spent the largest 
                    amount of money on the many and varied attractions and 
                    services during the festival. These observations were 
                    further reinstated by KPMG. An economic impact study 
                    conducted by the group established that number of visitors 
                    had grown from 980,000 in 1999 to more than 2,000,000 in 
                    2001, and this figure is expected to increase every year. 
                    Jeddah Summer Festival is considered the biggest family 
                    entertainment event in Saudi Arabia. <br>
                    <br>
                    Besides local visitors, the event organisers expect a lot of 
                    tourists from within the GCC. Keeping in mind the growing 
                    potential of this sector, the Saudi government is trying to 
                    add more attractions to this event. Besides the laser shows, 
                    fireworks, parachute jumps, ice skaters, circus, musical and 
                    other cultural events, this year's campaign is unique as it 
                    includes several innovative programmes such as a media 
                    centre that will serve local and international press, as 
                    well as daily broadcasts on MBC FM radio and weekly coverage 
                    on MBC and other satellite television channels. <br>
                    <br>
                    The festival is expected to account for a tremendous boost 
                    to Jeddah's image as a tourist destination in addition to 
                    its status as the gateway to the two holy cities. Another 
                    outstanding feature of this year's festival will be the 
                    establishment of a Surprise Village over an area of 65,000 
                    sq mt, which will comprise 100 different events to attract 
                    visitors. The aim is to establish Jeddah as the leisure 
                    capital of Saudi Arabia.
            </font></p>

                    <p align="justify"><b>
                    <font face="Verdana" size="2" color="#CC3333">
                    United Arab Emirates:</font></b><font face="Verdana" size="1"><br>
                    <b>Dubai Summer Surprises</b><br>
                    <br>
                    Dubai Summer Surprises (DSS) is an annual event held by the 
                    Government of Dubai in order to boost tourism and shopping 
                    in Dubai, with the long-term objective of turning Dubai into 
                    a year-round tourism destination. Proclaimed as the summer 
                    capital of fun, DSS aims to energise trade and tourism 
                    during the relatively lean summer months. Undoubtedly, DSS 
                    brings with it a lot of colour and excitement, but the event 
                    has also metamorphosed the summer season in the country. It 
                    has boosted the country's trade by drawing vacationing 
                    families to hotels, shopping malls and entertainment centres, 
                    particularly from elsewhere in the Gulf.<br>
                    <br>
                    <img border="0" src="im27.jpg" align="left" hspace="5" width="250" height="122">Business 
                    has been doing particularly well in its first three weeks of 
                    the ongoing DSS 2004. According to official figures, DSS 
                    2004 has surpassed records set last year in business, retail 
                    and tourism sectors. Till date, sales in shopping malls are 
                    up 10 per cent in these first three weeks as compared to 
                    last year, and hotel traffic is up to 82 per cent, according 
                    to the statistics. The high visitor traffic generated by DSS 
                    2004 in its opening weeks has greatly contributed to Dubai's 
                    summer economy. Nearly all sectors associated with the 
                    events--tourism, travel, retail, trade and exchange, 
                    restaurants, hotel and hospitality--are reaping benefits.<br>
                    <br>
                    Even though it's just the beginning of the festival, hotels/ 
                    hotel apartments around the city have reported an occupancy 
                    rate between 75 and 98 per cent. The participating hotels 
                    expect the occupancy rates to surge during the coming weeks 
                    of DSS 2004. Flowing with the number of bookings, most 
                    hoteliers expect their occupancy rate to soon be 100 per 
                    cent. In the first two weeks of the festival, Metropolitan 
                    Hotels International has recorded 98 per cent occupancy 
                    across all its properties - Metropolitan Palace Hotel, 
                    Metropolitan Sheikh Zayed Rd, Metropolitan Beach Hotel, 
                    Jumeirah and Metropolitan Deira. <br>
                    <br>
                    Same goes for InterContinental Dubai. The hotel achieved 87 
                    per cent occupancy in the month of June, and the figure is 
                    expected to reach 89 per cent in July and August. Most 
                    bookings are made by tourists from the AGCC countries, but 
                    there are a number of bookings form the UK and other 
                    European countries as well, the officials at the hotel 
                    reported. <br>
                    <br>
                    Other important hotel like Accor Hotels World Trade Centre, 
                    Dubai, is doing good business too. It has recorded 80 per 
                    cent occupancy at Novotel and Ibis has recorded 70 per cent 
                    occupancy. Coral Deira Hotel has recorded 75-80 per cent 
                    occupancy. <br>
                    <br>
                    According to the organisers, a lot of hard work and planning 
                    goes into making DSS a successful event. Besides the 
                    enormous investment made, the marketing strategy behind the 
                    growing popularity of DSS is commendable. This large-scale 
                    event, right from its opening ceremony till the closing, is 
                    organised and handled together by the government departments 
                    and the DSS office. Besides using all forms of the media, 
                    special road shows for DSS are organised in various 
                    countries like Bahrain, Kuwait and Saudi Arabia etc. in 
                    order to reach out to the audience. <br>
                    <br>
                    Right from Hong Kong to the United States, tourists flock to 
                    be a part of the DSS. Assessing the phenomenal success of 
                    DSS, officials are of the view that Dubai can be turned into 
                    an extremely hot tourist destination during any season. 
                    &quot;Summer events such as DSS have proved that UAE can be 
                    marketed as a year-round tourist destination,'' said Saeed 
                    Mohammed Al Nabouda, the chief executive officer of DSS. 
                    According to him, there has been an increase in awareness in 
                    what Dubai has to offer the international visitors, and that 
                    the city is now ranked eighth in the world for tourists to 
                    visit. &quot;The phenomenal growth in Dubai's tourism industry is 
                    a reflection of the government's commitment to actively 
                    diversify economy and to reduce dependence on oil revenue, 
                    which is a finite resource,'' he added.
            </font></p>

                    <p align="justify"><font face="Verdana" size="1">
                    <b>Dubai Shopping Festival</b><br>
                    <br>
                    Dubai Shopping Festival is an inseparable part of the DSS. 
                    Participating in DSS activities and shopping just seem to go 
                    hand in hand for tourists who come to be a part of the DSS. 
                    The success of the Dubai Shopping Festival (DSF) has helped 
                    enhance the country's profile on the global tourism map, not 
                    only as a leading shopping destination, but also has a 
                    tourism hub. Today, DSF attracts hordes of visitors from all 
                    over the globe. <br>
                    <br>
                    DSF is a time when the word sale is taken 
                    for granted in Dubai. Shopping malls, under the umbrella of 
                    the Dubai Shopping Malls Group (DSMG), not only give great 
                    discounts and bargains but also become hubs of 
                    entertainment, providing shows and hosting key events of 
                    different surprises. To make the DSF a hassle-free time, the 
                    Dubai Department of Economic Development (DED), the main 
                    regulatory body which organises and regulates trade and 
                    industry in the emirate, keeps a strict vigil on the shops 
                    that give discounts. Shops that do not adhere by the rules 
                    as set down by the DED can face action by the authorities 
                    and an investigation can be launched against them if 
                    customers lodge a complaint with the Department. <br>
                    <br>
                    DSF 2004, which was held from January 15 to February 15, 
                    2004, was a huge success. During this time, the festival 
                    registered a total of 3.1 million visitors, posting a growth 
                    of 6 per cent over 2.92 million visitors in 2003. The total 
                    visitor spend during DSF 2004 touched Dh5.8 billion 
                    (RO607.24 million), compared to Dh5.12 (RO536.05 million) 
                    billion in the previous year, thus registering an increase 
                    of 13.28 per cent. The retail spending for the same period 
                    posted a 169 per cent increase over the figures achieved in 
                    1996. The shopping malls contributed 58 per cent of the 
                    total sales achieved during DSF 2004. Restaurants 
                    contributed 22 per cent of the total visitor spend while 
                    hotels and hotel apartments accounted for 14 per cent. 
                    (Figures were made public by DSS CEO Saeed Mohammed Al 
                    Nabouda.)
            </font></p>

                    <p align="justify">
                    <font face="Verdana" size="2" color="#CC3333">
                    <b>State of Qatar: </b>
            </font><font face="Verdana" size="1">
                    <br>
                    <b>Qatar Summer Wonders </b><br>
                    <br>
                    Just like its neighbours, the state of Qatar--which has one 
                    of the world's largest reserves of natural gas--is embarking 
                    upon a steep travel and tourism growth trajectory. At the 
                    World Travel &amp; Tourism Council summit in May 2004, Akbar Al 
                    Baker, head of the national tourism development company and 
                    Qatar Airways, unveiled a US$15 billion (RO5.77 billion) 
                    tourism master plan designed to make Qatar a &quot;world-leading 
                    high-quality tourist destination''. He said the plan is 
                    aimed at the &quot;high-end tourist market'', and will offer 
                    beach resorts, sun, desert landscapes, an Arabic cultural 
                    heritage, luxury hotels, conference facilities and major 
                    international sporting events - &quot;all in a very safe 
                    environment''. <br>
                    <br>
                    <img border="0" src="im23.jpg" align="right" hspace="5" width="200" height="146">He 
                    added the plan represents one of the largest investments in 
                    tourism in the Gulf region and will help more than double 
                    tourism growth from roughly 400,000 annual visitors to in 
                    excess of 1 million by 2010. Thus, a massive PR and 
                    marketing drive has been launched and millions of riyals are 
                    being pumped into the tourism sector. However, according to 
                    officials, no payback is expected immediately, or in the 
                    near future. The only immediate return expected is exposure, 
                    regionally as well as globally. <br>
                    <br>
                    The annual Summer Wonders festival (QSW), which started in 
                    2002, is part of this larger plan - an important component 
                    of Qatar's Tourism Master plan, &quot;to establish Qatar as a 
                    high quality destination for cultural tourism, beach 
                    resorts, shopping, business, education and sports,'' says 
                    Tourism Authority CEO Fred van Eijk. The intention behind 
                    QSW is not just inviting visitors, but to urge residents to 
                    stay behind during summer. Even though Qatar started later 
                    than countries like Oman and the UAE, the QSW 2002 was a 
                    huge success and a lot of people stayed back in the country, 
                    avoiding large expenses related to overseas travel. Besides 
                    the local people, the festival was also able to attract 
                    foreign visitors, thus activating tourism in the country.
                    <br>
                    <br>
                    This year, Qatar is all set for high visitor traffic during 
                    Summer Wonders 2004, which is in full swing. <br>
                    <br>
                    Having played host to roughly 2.5 million tourists in 2003, 
                    organisers of the festival expect a two-fold increase in 
                    visitors to Qatar Summer Wonders 2004. This year's festival 
                    once again targets the huge GCC tourism industry, with 
                    hotels, shopping malls and the country's national airline 
                    announcing special incentives and offers to lure both 
                    residents and visitors. <br>
                    <br>
                    According to officials at the Qatar Tourism Authority (QTA), 
                    many visitors from Levant countries and the Indian 
                    subcontinent visit the country during the event but the bulk 
                    of tourists are from the GCC. &quot;Awareness about tourism 
                    potential in Qatar is steadily rising and the country's 
                    vibrant economy has ensured a high flow of business visitors 
                    throughout the year,'' says Fred van Eijk, CEO, QTA. &quot;The 
                    QSW takes that reputation a step further by highlighting the 
                    immense facilities and infrastructure we have in place. We 
                    are pleased that leading corporate names in the country's 
                    economic development have thrown their weight behind this 
                    campaign'', he added. <br>
                    <br>
                    Economically, QSW doesn't contribute much to the sate 
                    economy. Being in its initial years, &quot;this festival is not 
                    an opportunity for QTA to make profit - we have been given 
                    government subsidies to provide the people of Qatar with 
                    entertainment. Many of the activities are free and those 
                    that are not are (offered) at a nominal fee. However, huge 
                    economic benefits have been gained by the participating 
                    outlets. For every QR50 (RO5.29) purchase at these outlets, 
                    a coupon is given which gives the buyer a chance to be a 
                    lucky winner in the weekly raffle draws with gifts totalling 
                    QR500,000 (RO52,884) in value. In the first week alone, 
                    ticket sales came to QR2.8 million (RO296,148), which shows 
                    that people have seized the opportunity to go out and shop. 
                    I personally and in the newspapers have received comments 
                    from many shop owners stating that their sales during the 
                    festival are significantly higher than in normal days'', 
                    according to van Eijk. <br>
                    <br>
                    The hospitality industry has also gained significantly by 
                    the festival. Some of the leading hotels in Qatar have 
                    reported that they are nearing 100 per cent room occupancy 
                    for the month of July, as the country celebrates the QSW 
                    2004. The top hotels, which include, Movenpick and 
                    InterContinental, pointed out that demand for rooms was so 
                    high that the hotels were working round-the-clock to 
                    accommodate as many visitors as possible. Others hotels such 
                    as Al-Sadd Merweb and Mercure also have high occupancy 
                    figures due to the number of performers, organisers and 
                    international media staying in these hotels.<br>
                    <br>
                    Top hotels such as Ritz-Carlton, Sheraton, Marriott, Ramada 
                    and Movenpick have slashed their room rates by more than 50 
                    per cent and are offering attractive packages to suit budget 
                    visitors. Most hoteliers believe that, unlike the past, 
                    summers have become a time for good business. The festival 
                    has given a fillip to the sagging hospitality industry 
                    during the lean summer months. &quot;With QSW, we reinforce our 
