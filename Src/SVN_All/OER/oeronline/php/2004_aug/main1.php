            <font face="Verdana" size="4" color="#FF0000">Summer extravaganza</font><p align="justify"><font face="Verdana" size="1">
            <b>GCC countries sure believe in taking fun seriously. Started a few 
            years ago, the region's increasing party spirit and summer festivals 
            have had a positive effect on the economy. Various reports estimate 
            the region's summer fiestas to have had a combined economic impact 
            in the range between RO3 billion and 5 billion. Shuchita Kapur 
            reports</b><br>
            <br>
            <img border="0" src="im30.jpg" align="left" hspace="5" width="150" height="300">Come 
            summer, and it's time again for summer festivals in the six GCC 
            nations. Whether it's the Khareef Salalah Festival in Oman, Dubai 
            Summer Surprises (DSS) in the UAE or the Jeddah Ghair in Saudi 
            Arabia, the word is official: Arabs have a healthy appetite for 
            entertainment and the region's popular summer festivals have had a 
            gluttonous impact on the regional economy. These mega entertainment 
            and shopping events have converted the once lean summer months into 
            a time bubbling with activity and fun. <br>
            <br>
            This newfound synergy between tourism, entertainment and shopping is 
            certainly paying off dividends. Tourist inflow graph is curving 
            upwards, vacant hotels are recording a high occupancy rate, shopping 
            malls are full of buyers and many unemployed people land up jobs. 
            With each passing year, these summer festivals are gaining more and 
            more popularity in all the six countries in the GCC. Having realised 
            the importance of the tourism sector for the healthy state of the 
            economy, governments in these countries are focusing on marketing 
            such events to attract monetary inflow into the countries and, 
            today, all the countries are members of the World Tourism 
            Organisation (WTO). <br>
            <br>
            After decades of relying on oil and gas, the Gulf countries of Oman, 
            Kuwait, Qatar, Bahrain, United Arab Emirates (UAE) and Saudi Arabia 
            are preparing for the post-oil era. The economic model they have 
            charted is patterned along the lines of Singapore, which, having few 
            natural resources, created an economy based on services and tourism. 
            The UAE, probably the least oil-rich of the Gulf countries, took the 
            lead in the late 1980s and is aiming to become the regional hub for 
            tourism. Oman, Qatar, Kuwait, Bahrain and Saudi Arabia are following 
            suit to seek a piece in this economic pie. <br>
            <br>
            Tourism to the Middle East is growing faster than any other region 
            of the world, with an increase of 17 per cent in arrivals in 2002 
            and 10 per cent in 2003, a year that saw negative tourism growth 
            worldwide. According to WTO figures, by the year 2020, visitor 
            traffic to the Middle East region will increase to 69 million. The 
            following graph showcases world trend in tourism. <br>
            <br>
            <img border="0" src="im24.jpg" align="right" hspace="5" width="200" height="135">This 
            is the time to get active and promote tourism in the region. 
            Although all countries in the region focus on promoting tourism 
            throughout the year, they have particularly taken up the task of 
            doing it during the summer months. Dubai is doing it by giving an 
            air-conditioned environment to visitors even when the temperature is 
            soaring to 50-degrees Celsius whereas Oman is exploiting its natural 
            beauty and the cool climes of its Dhofar region to attract tourists. 
            Providing state-of-the-art tourist infrastructure with numerous 
            world-class hotels, shopping spectaculars with great cultural events 
            and sports have taken the form of summer festivals in these 
            countries. <br>
            <br>
            However, while individual countries are doing tremendous work with 
            regard to giving a major tourism push to their economies, what the 
            region lacks is perhaps a Schengen Visa (see box) kind of impetus to 
            take tourist inflow into the region to even greater heights. In 
            fact, many of the GCC countries have relaxed their visa regulations 
            in an attempt to increase tourist arrivals. Moreover, GCC nations 
            have their annual fests more or less during the same summer months - 
            it would make perfect business sense for these nations to combine 
            forces and offer tourists--both from within the region as well as 
            from outside--a chance to stay and move around freely in GCC 
            countries. <br>
            <br>
            With the GCC customs union now a reality, a combined Gulf visa may 
            not be just a pie-in-the-sky. If the governments of the GCC 
            countries do, sometime in the future, decide to allow a combined 
            visa for tourists, even merely during the summer months, the total 
            positive economic impact on the region will definitely be far 
            greater than the sum of the parts. Furthermore, it will provide the 
            region's hospitality and the airline industries the much needed 
            impetus in terms of additional room occupancy and traffic inflow.<br>
            <br>
            Let us have a closer look at the summer festivals in these countries 
            and the impact they have on the region's economy.
