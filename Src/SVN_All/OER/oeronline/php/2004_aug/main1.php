                    <b>Hala Shopping Festival</b><br>
                    <br>
                    Tourism is a relatively untapped industry in Kuwait. Most 
                    visitors coming to the country are on business rather than 
                    leisure trips. Business visitors account over 90 per cent of 
                    travellers coming to the country. Reason: the lack of 
                    attractions, which are essential for the development of a 
                    leisure tourism industry. Oil production being the dominant 
                    economic activity in the country, developing the tourism 
                    sector has not been the primary agenda of the government.
                    <br>
                    <br>
                    <img border="0" src="im29.jpg" align="right" hspace="5" width="200" height="131">However, 
                    things have fairly changed for the oil rich country after 
                    its entry into the World Tourism Organisation. According to 
                    recent data available from the Organisation, visitor traffic 
                    to Kuwait was 77,000 in 1998, which has increased each year 
                    since then. International tourism receipts have also 
                    increased year on year with the 1999 figures at US$243 
                    million (RO93.46 million). <br>
                    <br>
                    Although late, there is an increasing awareness to boost 
                    tourism in the country by attracting non-business visitors 
                    to the country. The Kuwait Tourism Services Company (KTSC), 
                    which was set up to promote the country in both regional and 
                    overseas markets together with the Kuwait Hotels 
                    Association, Kuwait Airlines, the Ministry of Information 
                    and other industry stakeholders, is working to diversify 
                    Kuwait's visitor profile. The development of resorts and 
                    shopping attractions and cultural and entertainment 
                    extravaganza to entice leisure traveller underline the 
                    increasing importance being accorded to the tourism sector. 
                    One such step towards attracting potential fun seeking 
                    visitors was the introduction of the Hala Shopping Festival 
                    in 1999. <br>
                    <br>
                    Unlike the other constituents of the GCC, Kuwait organises 
                    its most popular event, the Hala Festival in the month of 
                    February. The first Hala Fest was an absolute success. It 
                    boosted the hotel and retail sectors and since then has 
                    become an annual event. Not only in its timing is Kuwait's 
                    fest different from its partner countries but also in its 
                    objective. Whereas countries like Oman and the UAE aim to 
                    attract visitors from all over the world, the main market 
                    for the Hala Festival remains the GCC. <br>
                    <br>
                    The Hala Shopping Festival offers a great mix of 
                    entertainment and shopping. According to official figures, 
                    visitor traffic during Hala Festival 2000 improved by 5 per 
                    cent in comparison to the inaugural festival in 1999. The 
                    festival is expected to boost the Kuwaiti economy and around 
                    100,000 foreign tourists visited the country during the Hala 
                    Festival in 2003. <br>
                    <br>
                    February is a perfect time to visit Kuwait, especially when 
                    Hala Festival is in full swing. The arrival of migratory 
                    birds and seasonal animals during this time adds further 
                    attractions. This month-long celebration of cultural and 
                    entertainment extravaganza offers a great opportunity for 
                    young and old alike to participate in various events like 
                    the kaleidoscope show and shopping carnivals etc. It is also 
                    an opportune time to win prizes at the many raffles and 
                    contests held during the period. <br>
                    <br>
                    Even though Kuwait's Hala Festival is making a valuable 
                    contribution to the promotion of the country, the event 
                    still hasn't made a dent into the other regional shopping 
                    events. What is needed here is proper marketing of the 
                    event. &quot;Our regional neighbours have proved that marketing 
                    really can work, and it is up to all of us in the tourism 
                    and hospitality sector to position Kuwait as a multi-faceted 
                    country, one that has considerably more to offer than just 
                    the Kuwait Towers, the usual image in people's minds,'' says 
                    Hannes Yaghi of Le Meridien hotel.<br>
