                    of the Gulf's best kept secrets. The drama shows, sports, 
                    arts and crafts, photography exhibitions and music concerts 
                    that form part of the Hala Festival have much potential to 
                    attract tourists,'' he added. If promoted properly, the Hala 
                    Fest in the long run can make Kuwait a much sought-after 
                    tourist destination.'</font></p>

