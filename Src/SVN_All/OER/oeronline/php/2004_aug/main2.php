<p align="justify"><font face="Verdana" size="4" color="#FF0000">Managing 
cultural diversity in a global world<br>
</font><font face="Verdana" size="1"><b><br>
The survival of transnational telecom companies will depend on their flexibility 
in managing cultural diversity. This not only entails the successful management 
of a multi-cultural workforce in a global context, but also means being able to 
vary services across cultures. Edward Burman justifies his point.</b></font></p>

<p align="justify"><font face="Verdana" size="1">
<img border="0" src="im32.jpg" align="left" hspace="5" width="250" height="135">For 
telcos with global ambitions, success in the next 20 years will stem from 
successful joint ventures and alliances. But while it is a simple matter to draw 
lines across the globe in the manner of the 19th Century colonial powers and to 
devise a Concert, an Atlas, or a Unisource, there is no guarantee that such 
alliances will thrive - or even endure. Where once a global company such as 
Coca-Cola simply sold its product or imposed a taste, and multinationals geared 
the names or colours of identical products to the results of market research, 
the survival of transnational telcos will depend on flexibility in managing 
cultural diversity.<br>
<br>
Primarily, this entails the successful management of a multi-cultural workforce 
in a global context. But it also means being able to vary services across 
cultures: not simple marketing ploys imposed from outside, but an understanding 
of how culture drives differences from within. A simple example of this is the 
way in which different cultures use the phone: an American walks into his 
apartment after a week away and switches on the answer-phone; an Italian rings 
his mother. One requires an add-on device; the other needs single-number 
dialling and favoured-number discounts. These differences may appear trivial, 
but they are profoundly culture-driven. <br>
<br>
The development of genuinely transnational business organisations, therefore, 
requires managerial approaches and systems which allow for variations deriving 
from such diversity. This might be 'national' cultural diversity between 
nations, races or ethnic groups (e.g., in a two-nation joint-venture), 
intra-national diversity involving the range of cultures within a single nation 
(e.g., in the UAE, where expatriates constitute a majority of resident 
population), or internal cultural diversity where managers need to deal with 
foreign-owned transnational companies in their own country (e.g., an Omani telco 
manager dealing with a German manufacturer in the Sultanate). <br>
<br>
<img border="0" src="im31.jpg" align="right" width="150" height="108">All this 
is well known, and there is indeed a burgeoning literature on the management of 
cultural diversity. But the problems go deeper than is often appreciated: it is 
not simply a matter of minding manners or learning to deal with varying 
attitudes to punctuality. These are the surface manifestations of much deeper 
differences in mental structures. A few examples will make this clear. <br>
<br>
<b>Negotiating Alliances </b><br>
In a world in which cross-cultural joint ventures and alliances are essential, 
problems of ethics and trust will loom large. How is it possible to achieve a 
balance between the necessary and the contingent in business ethics, or in other 
words, to allow for flexibility between a strong corporate ethic and the need to 
adapt to difficult local conditions? And how can we learn to build a lasting 
trust relationship with people from a different culture? How can managers going 
to the negotiating table be prepared for the very different styles they will 
face? <br>
<br>
It is not merely a question of setting bargaining ranges, toning down 
confrontational styles, or following pre-established rules. That is sufficient 
for making a deal, but not for setting up a permanent alliance. It is essential 
to grasp the deep structures--religious, social, ethnic and ethical--which 
influence the way the opposite party will reason, the way they will react to 
different presentational styles, what they expect and how they listen.<br>
<br>
This requires a level of genuine understanding which goes beyond 
rapidly-acquired skills. Recent studies have shown how an inherent sense of 
cultural superiority is often enough to undermine European joint-ventures in 
Third World countries even when extensive training has been provided. Such 
'superiority' emanates from non-verbal aspects of behaviour like the tone of 
voice and body language, which few people other than accomplished actors are 
able to control.<br>
<br>
If, then, as this would suggest and has recently been asserted in telco 
documents on cultural diversity (e.g., by BT and France Telecom), humility is a 
key factor, how is it possible to inculcate this quality in managers whose 
education has often prepared them for anything but humility? <br>
<br>
<b>Human Resources </b><br>
The global operator obviously needs managers capable of working globally. Some 
European telcos are recruiting 'non-nationals' in order to resolve their 
problems quickly, but how does a human resource specialist trained in his own 
culture, who
<img border="0" src="im33.jpg" align="left" hspace="5" width="150" height="120">can 
make a rough assessment of a candidate's capabilities in a brief interview, deal 
with the problems of recruiting staff in other cultures? How valid is 
psychological testing when applied cross-culturally? How much do most human 
resource managers know about other school and university systems? <br>
<br>
Suppose a German manager needs to choose between, say, a Finn, an Italian and a 
Portuguese. That would require an awareness not only of the very different 
education systems in European countries, but the ways in which educational 
background influences patterns of thought and managerial style: how, for 
example, education underlies the way in which the same conflict might be 
addressed in France by seeking orders from a superior, in Britain by sending the 
people in conflict on a management course, and in Germany by employing a 
consultant. <br>
<br>
Assuming for a moment that these problems can be resolved, how might the issue 
of dual allegiance be tackled? For, the employment of local managers 
necessitates the creation of loyalty on their part to a distant entity with 
culturally diverse norms and assumptions. Even a long-term expatriate who is 
nominally still of the same nationality but has in fact 'gone native' might 
respond to an order in this way: ''I'm sure my local employees won't like this, 
so I won't tell them and try to smooth over the issue in some other way.'' It 
can be much more difficult for the locally employed manager, especially under 
stress. <br>
<br>
<b>Everyday Work</b><br>
Then there is the nitty-gritty of everyday working together, the problem of 
creating the rituals, the back-room humour and the 'off-stage' relationships 
which are so vital to harmonious corporate life. Company jokes and in-group 
stories, for example, are notoriously difficult to translate into other 
cultures: what sounds laudable to a Briton can seem risible to an Italian. 
Companies which contrived to impose a global corporate culture, such as IBM, did 
not face the insidious cultural problems of a transnational organisation. <br>
<br>
Language is another problem. Although it might appear that the use of English as 
the common working language of the international teleco community favours native 
English-speakers, this can turn into a disadvantage when one of them is unaware 
of the problems that a regional accent or rapid speech might create, and how 
linguistic confidence can be perceived as a manifestation of quasi-colonial 
arrogance. Non-conformity with what might be termed the 'industry pidgin' can 
also generate unexpected tensions. <br>
<br>
Worse still, behind the words on the surface lurk centuries of cultural and 
ideological rivalry which has often exploded into war. At moments of strain, 
when a minor conflict might have irreversible consequences, simmering 
stereotypes and prejudices boil up. Studies of cross-cultural teams indicate 
that often it is the most superficially similar cultures which in the end 
experience the greatest traumas: while differences such as those between the US 
and Japan are obvious, serious problems often occur where they are least 
expected-say, between Britain and Denmark-and warning signals are neither 
perceived nor acted upon. In a world as competitive as that of the telcos, 
nothing can be taken for granted. <br>
<br>
<b>The Way Forward </b><br>
<img border="0" src="im34.jpg" align="right" hspace="5" width="150" height="114">Cultural 
training is essential to avoid potential conflict, and to improve the disastrous 
failure rate of joint-ventures in the recent past. In fact, most telcos with 
global ambitions now provide cross-cultural training in order to create 
genuinely international managers. This sometimes involves in-house training, and 
is also provided by consultants and business schools. Yet, much of this training 
deals with the traditional, superficial problems without seeking to explore the 
deep causes of underlying cultural differences. Another problem is that much of 
the research and background material is rapidly outdated as the pace of change 
accelerates. <br>
<br>
An innovative approach has been taken by the collaborative venture known as 'Euroteam', 
started in 1991 by the five main European operators (BT, Deutsche Telekom, 
France Telecom, STET and Telefonica de Espana) with the aim of contributing by 
means of the organisation of regular workshops and exchanges to the 
internationalisation of telco operators. Euroteam was later expanded to include 
all European operators who were members of ETNO (the organisation of European 
Telecoms Network Operators), and at present has about 11 members ranging from 
Finland, Norway and Denmark to Portugal.<br>
<br>
Euroteam runs a series of international workshops-hosted alternately by the 
members-whose focus is on Europe and Telecommunications. There is a regular 
programme called 'Working Across Cultures', and there has been a successful 
version of a more theoretical workshop called 'Understanding Cultural 
Diversity'. Other programmes also include at least one session on cultural 
diversity. These workshops allow managers from member companies to learn and 
work together, and thus facilitate cross-cultural personal networking even 
within the ambit of fierce competition.<br>
<br>
The problem of devising some form of training within the industry has also been 
recognised at the inter-governmental level, where 'Cross-Cultural Training and 
Education' has been included among the Global Information Society projects of 
G7. <br>
<br>
<b>Conclusion</b> <br>
No comprehensive solution to the problems of cultural diversity in the context 
of the telecommunications industry has yet been conceived. Indeed, there has 
been little specific research. Yet, it is clear that preparation for the 
successful management of such diversity in all its ramifications will be a vital 
component of long-term success in the global market. For, while business is 
already global, management remains culture-bound. </font></p>

