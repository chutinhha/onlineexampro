<p align="justify"><font face="Verdana" size="4" color="#FF0000">Russian 
roulette</font><font face="Verdana" size="1"><br>
<br>
<b>Russia's oil industry is in turmoil. Middle East oil producers will be 
greeting every new twist and turn in the unfolding saga with loud cheers. 
Richard Dean reports</b><br>
<br>
Russian oil magnate Mikhail
Khordovsky has plenty to
worry about as he sits in his
cell in a Moscow jail. First, he
must worry about saving his
own skin - he is facing charges of fraud
and tax evasion that could see him jailed
for years. Second, he must worry about
saving Yukos, the giant Russian oil firm
in which he owns a 32 per cent stake.
Finally, he must worry about saving
Russia's entire oil industry - if not from
collapse, then at least from a significant
and damaging retrenchment.
<br>
<br>
This third point may not be top priority
in Khordovsky's troubled mind, but
it is very much at the forefront of Middle
East oil producers' thinking. The crisis at
Yukos threatens to derail the phenomenal
growth that has gripped Russia's oil
industry in recent years. If Yukos goes
under, the Russian oil boom could suffer
serious damage. That is encouraging
news for its Arab rivals.
<br>
<br>
First, the short-term consequences. Yukos must pay $3.4 billion (RO1.31 billion)
for taxes it evaded by channeling
money through Russia with investment
tax breaks. On June 29, a Moscow court
ordered immediate payment, drawing a
line under months of legal appeals over
whether and when Yukos should pay
the back taxes. On July 1, court marshals
arrived at Yukos' headquarters and
ordered it to pay within five days.
Yukos' bank accounts were frozen.
<br>
<br>
Oil traders fear this lack of cash flow
could prevent Yukos from sticking to its
export commitment.Yukos is a major
global player, pumping 1.7 million barrels
per day (over 2 per cent of world
output), putting it somewhere between
Libya and Kuwait in importance. If this
supply shuts down, it will send oil prices
skyward. Indeed, fears that this could
happen have helped sustain prices close
to $40 per barrel (/b) in recent weeks.
<br>
<br>
If Yukos does fail to maintain production,
Middle East producers will
gain from higher prices and higher output
in the short term, as Arab heavyweights
such as Saudi Arabia and the
UAE are among the only producers in
the world with spare output capacity at
the moment.
<br>
<br>
However, the potential long-term
consequences will be the cause of greatest
cheer in Riyadh, Abu Dhabi and
Tehran. The Yukos case has done
untold damage to Russia's image as a
stable business environment. Khordovsky
claims his arrest was politically
motivated after he refused to support
President Vladimir Putin in recent elections.
Russian prosecutors, of course,
deny this. But the entire episode has
sent a powerful negative message to
potential investors, just as oil majors
were beginning to pour serious money
into Russia. BP's new Russian joint venture,
TNK-BP, is expected to produce
1.5 million barrels of oil equivalents per
day this year, giving BP 750,000 barrels
or about 20 per cent of the British company's
total global output.
<br>
<br>
&quot;Russia must be careful not to give
negative signals to investors that have
to make big investments,&quot; says Claude Mandil, the head of the International
Energy Agency, the West's energy
watchdog. The World Bank confirmed
in a recent report that some of the bad
publicity surrounding Yukos is already
beginning to deter foreigners from ventures
in Russia.
<br>
<br>
This is great news for OPEC producers.
They have seen their market
share eroded from around 42 per cent of
global output in the late 1990s to around
38 per cent just three years later. Rising
Russian production-boosted by foreign
investment-was the main cause.
<br>
<br>
Middle East oil producers would
never admit it in public, but they must
surely crack a wry smile at the start of
the young upstart now hitting the rocks.
OPEC producers such as Saudi Arabia,
Iran and the UAE have enjoyed a troubled
relationship with Russia since the
former Soviet Union became a major
player around the turn of the millennium.
They have clashed over Russia's
steadfast refusal to curb production in
times of weak prices-notably after September
11, 2001-leaving OPEC members
to cut their own output to prop up
prices alone. This left Russia to reap all
the price benefits of OPEC's actions
without any of the sacrifice in output.
At the time, OPEC made little attempt
to hide its disgust. Middle East oil ministers
are noted for their long memories.
<br>
<br>
What is the likely outcome? As OER went to press in late July, Yukos
had managed to stick to all its supply
commitments. On July 9, Russian
Industry and Energy Minister Viktor
Khristenko insisted Russian oil exports
would be unaffected by the Yukos
scandal. &quot;There is no anxiety about
exports at the moment, no requests
have been cancelled.&quot;
<br>
<br>
Certainly, it will be in the Russian
government's interests to prevent Yukos from collapsing, whatever
President Putin's personal agenda may
be: Yukos accounts for some 4 per cent
of the Russian economy. Even so, it will
take more than bullish comments from
politicians such as Khristenko to allay
the fears of the global oil industry.
Sources in Moscow believe that, ultimately,
a deal will be struck that saves
Yukos and, quite possibly, Khordovsky
too. But it may already be too late to
save the reputation of the Russian oil
industry, and for that the Middle East's
producers will be profoundly grateful.<br>
<br>
<i>The author is director of Middle East Analysis,
a Dubai-based economic consultancy.</i></font></p>

