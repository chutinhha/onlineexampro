<p><b><font face="Verdana" size="1">Progress 2004-05 unveiled by His Excellency Juma bin Ali bin Juma al-Juma</font></b></p>

<p align="justify"><font face="Verdana" size="1">
<img border="0" src="im47.jpg" align="left" hspace="5" width="300" height="115">Every year, the Sultanate celebrates July 23 as 
the Accession Day of His Majesty
Sultan Qaboos Bin Said. It's a day of great significance and a moment to cherish
Oman's rapid progress under the wise and able leadership of His Majesty.
<br>
<br>
Every year, Progress, with its special commemorative edition, highlights
achievements of national importance and landmark events that reflect the
vision of His Majesty. In a special event, His Excellency Juma bin Ali bin Juma
al-Juma, Oman's Minster of Manpower, unveiled Progress 2004-2005 to mark
the 34th Anniversary of His Majesty's Accession.
<br>
<br>
His Excellency commended the publishers, United Press and Publishing
(UPP), on the efforts that go into planning and bringing out this Arabic-English
publication of repute. He emphasised that powerful communication media like
Progress are a great accomplishment as they reflect the strength of indigenous
endeavours showcasing Oman in its full glory.
<br>
<br>
Woven around the central theme of 'Technology', Progress 2004-2005
explores various aspects of technology and how it contributes to the overall
socio-economic development and nation-building. The annual coffee-table
book includes well-researched articles that comprehensively examine the vital
areas of economic development and information evolution. Progress 2004-2005
also addresses how public policy plays a critical role in bridging the digital
divide. It emphasises policies related to information and communication 
infrastructure,
including telecommunications and the Internet. Yet another innovation
in this year's edition of Progress is a Ready Reckoner at the end of every 
section,
where growth indicators and data pertaining to individual industry sectors
are highlighted in a simple-to-follow tabular format.
<br>
<br>
This year, along with a hard cover edition, the entire content and images of
Progress have also been captured in a CD-ROM presentation, which adds value as
an excellent archive material. ''It's a matter of great honour for us be 
associated
with the Accession Day through Progress. We strive hard to bring constant 
innovation
to this prestigious dossier'', explained Sandeep Sehgal, General Manager,
UPP, the leading publishing company in Oman that is responsible for Progress
and other premier publications such as Oman Economic Review and Al Mar'a.
<br>
<br>
The recent edition of Progress has been well received by industry leaders and
media professionals. ''As a publication, Progress has managed successfully to
reflect the achievements made in Oman throughout the years under the wise 
leadership
of His Majesty,'' said a senior spokesperson from Petroleum Development
Oman. The team behind this prestigious publication is extremely pleased with the
effort and is confident that in the years to come, Progress will continue to be 
the
most comprehensive source that showcases the progress and prosperity of Oman. </font></p>

