            <p align="justify"><font face="Verdana" size="1">Most organisations have difficulty
adapting to change. People usually
try to cope with change individually;
they rarely look at change as
part of teamwork. This results in
unhappy workers who are isolated
from one another. They look at
their jobs as a way to pay off their
bills rather than a 'LIVING'!
<br>
            <br>
            Employees should serve one another as 'internal' customers.
They should also have open channels of communication
so that confusion, frustrations and costly assumptions are
avoided. It has been proven that disengagement negatively
impacts employees' productivity, loyalty and retention and
overall organisation's profitability and customer satisfaction.
<br>
            <br>
            On the whole, it is the culture of the organisations that
really decides how employees react to different situations.
E.g., if the culture of the organisations is strict on dress codes,
most meetings will include the same on the agenda rather
than exploring creative abilities in the group. Organisations
must have realistic expectations so that staff are motivated.
While individual goals and differences must be considered,
employees should work for a selfless common good collectively
to ensure that organisational goals are met!<br>
            <br>
            Fish!Tales looks at those dysfunctional workplaces and
tries to combat this issue by learning from the fishmongers
at Seattle's Pike Place Market. The authors displayed how
the lessons can be adapted to different organisations such as
a hospital or a call centre. Each lesson was delivered through
a light-hearted and playful short story. Once these lessons are
applied, a business can be transformed into a place where
people 'play', are nice to one another, and teamwork can be
put into practice. The four main principles in the book are:
