            <p align="justify"><font face="Verdana" size="1">With the rapid growth in the population levels and the decline in 
            the interest rates, the demand for the real estate has been 
            increasing leading to the increase in the construction activity. 
            Credit facilities to the building and construction sector, which 
            were lying flat in the period from 1998-2001 picked up again in 2002 
            and 2003. In 2003, it showed an increase of 4 per cent over the 2002 
            levels indicating the increase in the building and construction work 
            activity in the country. One of the reasons that can be attributed 
            to the flat construction activity was the geo-political situation in 
            the region and now that stability seems to be returning to the 
            region, construction activity is showing signs of picking up again.
            <br>
            <br>
            Other factors that can add to the growth in the sector are the 
            strong oil prices and increase in the efforts of the government to 
            promote tourism which will require additional hotel and housing. 
            Also the inflow of the pilgrims for the annual Hajj demands high 
            quality buildings and hotels that can command premium. Real estate 
            in Madinah City has seen a tremendous growth in investments in 
            recent times. A new township has been planned, named Jowhara Taibah, 
            by Kreen Real Estate Development and Investment Group. The planned 
            township spreads over an area of 2.1 million sq. mt, divided into 
            1500 individual plots, which are being offered now to investors. 
            Plans are also underway to construct a number of residential towers 
            on Jabal Omar, a mountainous area close to the Grand Mosque 
            according to the chairman of the Makkah Construction Company (MCC), 
            at an estimated SR12 billion (RO1.23 billion). <br>
            <br>
            A Saudi private company, Rikaz, also announced that it is building a 
            luxury waterfront city around a manmade lagoon on the Gulf coast, on 
            the Al-Khobar-Dammam Corniche, at a cost of around $1 billion (RO385 
            million). The Fanar City, which is Saudi Arabia's first residential 
            project specifically geared toward non-GCC property owners, will 
            consists of residential, commercial, and leisure facilities. The 
            3.3-million sq. mt project, which will have the longest seafront in 
            Saudi Arabia, measuring over 4,000 meters, will consist of 
            waterfront houses, condominiums, apartments, hotels, a marina, 
            shopping and leisure conveniences, and Saudi Arabia's largest 
            private lagoon. <br>
            <br>
            The Ministry of Commerce and Industry has approved new regulations 
            for offering real estate shares for sale. The new regulations 
            consist of 13 articles, which include the terms and conditions for 
            the sale of shares by the real estate owners, either individuals or 
            firms. According to the new regulations, at least 20 per cent of the 
            shares should be floated in public offering and the announcement in 
            this respect should not contain any assurance regarding expected 
            profits. <br>
            <br>
            Saudi Government also initiated a massive low- and medium-income 
            housing programme. A housing project for medium income earners is 
            taking shape in Abraq Al-Raghama, east of Jeddah. This will cost 
            around SR320 million (RO32.86 million). The project with 423 housing 
            units is located close to the Makkah-Madinah Expressway and covers 
            an area of 250,000 sq. mt. The real estate is regarded as the second 
            largest sector having the local investments in the country. 
            According to National Real Estate Committee at the Council of Saudi 
            Chambers of Commerce &amp; Industry, it has been estimated that the 
            volume of real estate investments in Saudi Arabia has reached SR100 
            billion (RO10.27 billion). However, the sector still remains to be 
            cyclical and in Saudi's case, it is strongly linked to the oil and 
            other commodities markets - e.g., construction costs in Q1 2004 
            increased by 35 per cent because of a rise in iron and steel prices 
            in near term. In order to stop the rise in construction costs, Saudi 
            Arabia cut the tariff on reinforced steel from 20 per cent to 5 per 
            cent in a bid to control the rising iron and steel prices. <br>
            <br>
            With the decrease in the geo-political instability and increase in 
            population and tourism, Saudi Arabia's construction industry is 
            going to expand at a rapid pace. Construction industry has been 
            contributing 6 per cent of the GDP (2002) and it is expected to grow 
            at a faster rate aided keeping in mind the private investors' and 
            government's interest in this sector.
