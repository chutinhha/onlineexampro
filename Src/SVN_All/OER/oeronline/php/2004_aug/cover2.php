            <img border="0" src="im11.jpg" align="right" hspace="6" width="265" height="130"></font><font size="4" face="Times New Roman">T</font><font face="Verdana" size="1">he 
            property market in the Kingdom of Saudi Arabia is going through a 
            period of increased activity driven by an accelerating need for 
            affordable private housing and high-quality office space. Demand for 
            residential buildings grew by 2.7 per cent to an estimated SR30.3 
            billion (RO3.11 billion), on the back of a rapidly growing 
            population, availability of consumer loans from commercial banks and 
            easy long term credit from the Real Estate Development Fund (REDF). 
            In the sixth development plan, REDF granted 27,800 real estate loans 
            amounting to SR8,644 million (RO887.56 million) for financing the 
            construction of housing units for utilities. <br>
            <br>
            Apart from the demographic growth, increased liquidity has 
            stimulated the property market. Saudi investors have been bringing 
            back capital from abroad on account of adverse political 
            developments. Another important stimulant affecting the real estate 
            market is the Kingdom's steps to an economic reform program. The 
            sharp acceleration of property prices began soon after the issuance 
            of the foreign investment law which, for the first time, guaranteed 
            foreign investors' rights to buy land. Although there is little 
            evidence that foreigners are driving the property boom the prospect 
            of an injection of foreign capital clearly boosted the domestic 
            market, with investors buying up chunks of real estate to hold until 
            the market develops. <br>
            <br>
            In the capital Riyadh, population is growing at a rate of 8 per cent 
            a year, and the city's ground area is rapidly expanding- 
            particularly to the north. In the downtown area, Olaya, has seen a 
            surge in development of new office blocks and high quality retail 
            areas. The prime office space in Riyadh increased by about 40-50 per 
            cent in the past three years, but retail prices have appreciated at 
            a slower rate of 20 per cent over the past three years. According to 
            the country's Ministry of Municipalities and Rural Areas, the number 
            of permits in Riyadh totalled 7,101 in 2001, which included 6,638 
            permits issued for residential and commercial buildings. <br>
            <br>
            The Saudi cities of Mecca and Medina continue to flourish in the 
            property market with expansive projects being developed. Attracting 
            more than 2 million pilgrims for the annual Hajj, these two cities 
            have high quality apartment buildings and hotels which can command 
            premium values, owing to the combination of location and 
            accessibility to the pilgrimage cites. Properties in the centre of 
            the two cities command some of the highest real estate prices in the 
            world. In 2002, Riyadh based Aqar Real Estate Holding Co. bought six 
            plots of land overlooking the Prophets Mosque in Medina for SR1,250 
            million (RO128.35 million), covering 15,000 square metres in the 
            Bani Najjar district. It was the largest single sale of land in the 
            city since the executive committee for the development of central 
            Medina started auctioning property in 1990. <br>
            <br>
            Another large-scale project located in Mecca, the ZamZam tower, will 
            enable Muslims from across the world to own real estate in the close 
            proximity to the holy site. Aref Investment Group Co. and 
            International Ejarah and Investment Alliance signed a contract to 
            invest $390 million (RO150 million) in the project which will be 
            developed by the local Saudi Binladin Group (SBG) and will comprise 
            of four housing complexes and include 5,500 housing units. <br>
            <br>
            The ongoing development of the central areas of Mecca and Medina 
            will involve the demolition of deteriorating high-rise accommodation 
            and the construction of new facilities for the numerous pilgrims due 
            to arrive in the kingdom every year. In Jeddah, investors are keen 
            to acquire property, attracted by the potential returns. Apartment 
            and office buildings are much sought after, as they provide high 
            rental returns and prospects for capital appreciation. According to 
            the Ministry of Planning, the prime office space in Jeddah has 
            increased 30 per cent in the past three years. Under the supervision 
            of the Ministry of Public Works &amp; Housing, there are two major 
            projects under development in Jeddah. These two projects are 
            expected to stimulate the property market and boost investments in 
            the undeveloped areas.
