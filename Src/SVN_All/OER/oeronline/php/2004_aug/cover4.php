            <font face="Verdana" size="2" color="#CC3333">STATE OF KUWAIT </font></b><font face="Verdana" size="1">
            <br>
            </font><font size="4" face="Times New Roman">A</font><font face="Verdana" size="1">fter 
            a dramatic recovery, Kuwait's economy is now reaping rich dividends 
            based on changing socio-political dynamics in the region. The one 
            sector that seems to be raking in more than the others is real 
            estate.
            According to a National Bank of Kuwait report, ample liquidity and 
            low interest rates were the main factors behind the pick up in 
            domestic investment activity, primarily in real estate and equity 
            markets. <br>
            <br>
            <img border="0" src="im15.jpg" align="right" hspace="6" width="200" height="147">Real estate sales rose to their highest level in years, both in the 
            apartments and commercial property market as well as in private 
            residential property. Lending by the government-owned Savings and 
            Credit Bank (SCB)--which provides subsidised loans to new 
            homeowners--continued to bolster activity, with supplemental loans 
            from commercial banks giving an additional boost. Consequently, 
            there was an increase in the number of construction permits issued 
            by the Kuwait Municipality, accompanied by a surge in the 
            construction of homes and apartment buildings. <br>
            <br>
            Some experts, however, say that the Kuwaiti real estate market is 
            already on a downward trend, with margins as low as 2 per cent. As 
            more and more Kuwaitis choose to repatriate funds home, and internal 
            investments on the rise, the market is flush with liquidity. The 
            municipality has also increased the size of rentable areas of 
            developments. Land values have appreciated sharply from KD20 
            (RO26.12) per sq. ft a few years ago to KD52 (RO67.91) per sq. ft. 
            at present.
            </font></p>
            <p align="center">
            <img border="0" src="im16.jpg" vspace="3" width="350" height="157"><font face="Verdana" size="1"><br>
            </font><font face="Arial" size="1">Source: Ministry of Justice</font></p>
            <p align="justify"><font face="Verdana" size="1">Yields from real estate developments, subject to the area and type 
            of development, have declined from 12 per cent to 6 per cent in 
            general. However, the Bahrain-based Islamic investment bank Gulf 
            Finance House (GFH) raised KD15 million (RO19.59 million) in private 
            placement for Kuwait-based Gulf Development Real Estate Co. 
            According to GFH, this is a firm and credible indication of the 
            continuing upsurge in the real estate sector of the Gulf region. The 
            company has been floated with the exclusive intention of channelling 
            investments into blue-chip real estate assets in the GCC countries.
            <br>
            <br>
            GFH CEO Esam Janahi has said that the success of the private 
            placement shows the confidence the market has in the growth of the 
            real estate sector of GCC, which is passing through an unprecedented 
            boom phase, opening up steady growth avenues for informed investors. 
            &quot;The fact that the private placement was oversubscribed many times 
            and raised KD5 million (RO6.53 million) more than the originally 
            intended KD10 million (RO13.06 million) is a vindication of the 
            strategic outlook of GFH towards the region's excellent real estate 
            investment opportunities,&quot; he said. <br>
            <br>
            ''We have been able to get on board an elite group of strategic 
            institutional and influential local and regional investors. We are 
            confident that with the support of our investors Gulf Development 
            Real Estate Co. will be successful in achieving high returns from 
            investing in blue-chip real estate assets in Kuwait as well as other 
            mega development projects in the wider GCC region,&quot; Janahi said. <br>
            <br>
            Among Kuwait's premier real estate projects is the Water Front Phase 
            5, developed by the United Realty Company. The fifth phase of the 
            water front development project is located in Al-Salmiya area, 
            covering 3 km from the Gulf of Kuwait. The project is nearing 
            completion with the opening of its second phase, a seafront leisure 
            and retail area containing a five-star hotel, restaurants, luxury 
            shops and entertainment centres and a world-class marina. <br>
            <br>
            <img border="0" src="im18.jpg" align="right" hspace="6" width="200" height="200">The new coastline phase, is linked to Marina Mall by an enclosed 
            panoramic pedestrian bridge across Gulf Street, completes a massive 
            three-year KD50 million (RO65.30 million) development. When fully 
            operational, the huge complex, covering an area equivalent to 84 
            football pitches, will be re-named 'Marina World' to reflect its 
            unprecedented range of upmarket leisure, entertainment and shopping 
            facilities available in one location. <br>
            <br>
            Marina World is a major development for URC, a part of Kuwait 
            Projects Company (Kipco). &quot;Marina World will be a complete 
            destination for local and expatriate families, as well as tourists, 
            with top-quality shopping, dining, leisure and entertainment 
            facilities all created with a unique synergy in a sweeping coastline 
            setting,&quot; URC managing director Omran Hayat said. The three-year 
            project has involved reclaiming some 185,000 sq mt of coastal land, 
            and building on a total site of 420,000 sq. mt each side of the 
            coastal road. New car parking areas in the second phase site will 
            provide extended car parking facilities for visitors to each part of 
            the Marina World complex. <br>
            <br>
            Currently, there is over-supply of commercial space in certain areas 
            in Kuwait, and many new buildings have a significant amount of empty 
            commercial space. However, owing to the expected increase in demand 
            for office space due to entry of foreign companies proposing to do 
            business with Iraq by establishing base in Kuwait and also due to 
            the proposed entry of foreign banks in the next couple of years, 
            commercial land prices and rentals are likely to increase moderately 
            over the medium term.<br>
            <br>
            Investment land prices increased sharply in 2002 due to increase in 
            the permissible built-up area. Increase in the permissible built-up 
            area means more apartments may be built which in turn will boost 
            returns on the plot, making it even more attractive to investors. 
            This resulted in a sharp increase in investment land prices 
            especially in the Salmiya and Hawally areas, which are prime 
            investment land areas. Land prices in the investment segment are 
            expected to further improve over the medium term. The rentals for 
            investment properties have also increased in 2002 though not at the 
            same rate as the increase in land prices. The rentals of investment 
            properties are expected to remain firm on account of the growing 
            expatriate population. <br>
            <br>
            Activity in the real estate sector increased substantially in 2002. 
            In 2002, real estate sales increased by 63 per cent compared to the 
            previous year to around KD2.28 billion (RO2.98 billion). Of the 
            total real estate sales in 2002, residential sales amounted to 
            KD1.48 billion (RO1.93 billion) and accounted for 65 per cent of 
            total real estate sales by value while the investment and commercial 
            segments accounted for 32.5 per cent and 2.5 per cent of real estate 
            sales by value respectively. In the first six months of the current 
            year, real estate sales amounted to KD1.24 billion (RO1.62 billion), 
            an annualised increase of 8.5 per cent over 2002. <br>
            <br>
            The increase in activity in the sector and the appreciation in land 
            prices and rentals have resulted in improved financial performance 
            by the companies in the sector. The aggregate profits of listed real 
            estate companies increased by 96.1 per cent to KD44.3 million 
            (RO57.85 million) in 2002 over the previous year. <br>
            <br>
            The trend 
            continued in 2003, with the aggregate profits of listed companies 
            higher by 53.6 per cent over the same period last year. A large part 
            of the growth in profits of real estate companies was on account of 
            profits on sale of investments and unrealised gain on account of 
            appreciation in value of investments. The prices of real estate 
            company stocks listed on the KSE have also reflected the improved 
            financial performance of the companies. <br>
            <br>
            Although the real estate market is witnessing a period of growth and 
            is currently expanding at a significant rate, there still remain 
            several obstacles that hold back the sector from growing even 
            faster. Some of these obstacles are: a general lack of information 
            and transparency in the sector, volatility in essential building 
            material prices, the lack of an organising body to regulate the 
            sector and the concentration of land holdings in the hands of a few 
            investors, which is especially true of the residential land segment. 
            These factors act as restraints in obtaining rapid solutions to 
            Kuwait's current housing problems. <br>
            <br>
            Information is essential for making investment decisions. Numbers 
            dealing with economic growth, sectoral growths, loan rates, the 
            number of building permits, real estate transactions are all 
            considered nonexistent or need further development to become 
            constructive in creating market awareness. Information available to 
            individuals is practically absent, and up to date information is 
            mostly unavailable. Another obstacle the real estate sector faces is 
            the lack of a prominent and well-defined active role for private 
            sector firms. <br>
            <br>
            The housing issue in Kuwait is too large for the 
            government to handle on its own. Therefore, the government should 
            provide incentives to the private sector for a more active role. The 
            Build Operate Transfer (BOT) projects or even the Built To Suit 
            (BTS) systems may be good tools for the government to utilise.
            </font></p>
            <p align="center">
            <img border="0" src="im17.jpg" width="350" height="101"><font face="Verdana" size="1"><br>
            Source: Central Bank of Kuwait; Figures in KD million;<br>
