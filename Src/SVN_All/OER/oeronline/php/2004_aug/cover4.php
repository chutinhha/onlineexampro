            <p align="justify"><font face="Verdana" size="1">Previously, the government was the sole player in the real estate 
            market and the private sector was kept out of the equation out of 
            the fear that speculative activities of the private sector would 
            lead to hikes in plot prices to over-inflated levels, which would 
            impair genuine development. It is undeniably vital for the private 
            sector to play a more dynamic role in alleviating the housing 
            dilemma, but it is also important that the government regulates such 
            activities. <br>
            <br>
            The volatility of building material prices is another key issue that 
            needs to be resolved. Volatile prices of building materials could 
            discourage new projects and can result in a slowdown in the 
            construction segment of the real estate sector. <br>
            <br>
            A major portion of the real estate sector drawbacks mentioned above 
            may be attributed to the lack of an absolute regulatory body that 
            governs the sector as a whole. Attempts at creating this body have 
            taken place but to no avail. <br>
            <br>
            The result of the numerous committee's 
            and conferences held by both private sector players (companies, 
            brokers and unions) and government bodies (representatives from the 
            Ministry of Commerce and Industry, the Chamber of Commerce and 
            Industry) was the establishment of Miqasa Real Estate Company that 
            would regulate the sector in terms of protecting the rights of 
            different players, providing sufficient statistical information 
            important for transparency and organizing general activity within 
            the sector. <br>
            <br>
            However, this project did not take off as it was met 
            with scepticism by real estate brokers, who felt that it may 
            infringe on their line of activity and in turn affect their profit 
            levels. <br>
            <br>
            Despite all these negatives, the growth prospects for the Kuwaiti 
            real estate market in the short to medium term appear promising due 
            to the large existing demand and the rapid growth in population, 
            which will ensure high growth rates in demand for real estate. Other 
            factors, such as low interest rates, boost to the local economy on 
            account of the reconstruction of Iraq, increased interest in 
            domestic investments due to depressed global markets, and the 
            establishment of new real estate funds, are likely to result in the 
            real estate segment performing well in the short to medium term. <br>
            <br>
            The 
            rate of growth in real estate prices in general would however be 
            lower as they have already appreciated significantly in the recent 
            past. Delays in receipts of UNCC payments and lower oil prices 
            could, however, act as growth dampeners. <br>
            <br>
            Kuwait still houses massive land plots in both residential areas as 
            well as commercial and investment plots that have not yet been 
            tapped. There also exists a large pool of unexploited resources from 
            private sector firms that have been hampered by regulations, 
            negligence and red tape. Although attempts to reform and develop 
            this crucial sector must be commended, more measures must be 
            employed to further drive this sizeable sector to a pioneering role 
            in further expanding the value added by the non-oil sector in 
            Kuwait's economy.
