                  <td width="100%"><font face="Verdana" size="4" color="#FF0000">Book
                    Review</font><p><span style="mso-fareast-font-family: MS Mincho"><font face="Verdana" size="2"><b>Brand mantras<br>
                    </b><br>
                    The big question for entrepreneurs, brand managers and marketing managers: Are you still marketing your brand like you did in 1999? Do you still think that the most effective way to build a magnetic brand is to advertise and advertise and advertise...? Well, if the answer is yes (or even a Ômaybe'), then this is the book for you. Co-authored by Matthew W. Ragas and Bolivar J. Bueno, The Power of Cult Branding breaks several marketing myths and compels you to rethink the way you are doing business.<br>
                    The authors, who are entrepreneurs and marketers themselves, have held forth their views and theories by compressing them into Ôseven golden rules of cult branding'. What sets the book apart from other marketing rulebooks is the authors' meticulous presentation of every concept by supporting it with behind-the-scenes stories, lessons and experiences of the nine cult brands that they have profiled.&nbsp;<br>
                    The book takes the reader through the triumphs, failures and marketing strategies of Oprah Winfrey, Apple Computer, Volkswagen, Linux, Star Trek, Jimmy Buffet, Harley Davidson, Vans and World Wrestling Entertainment, arguably the nine most powerful cult brands in modern times. Cult brands, as the authors tell us, Òmay not necessarily be the biggest and most well-known brands, but they are companies with the most fanatical and most loyal customer followings." The book brings to light interesting facts and anecdotes of each of the nine brands, which lend valuable insight to their marketing philosophy and phenomenal growth over the years. Another point that the authors have scored over other marketing books is their attempt to jog the reader's thoughts and concepts at the end of every chapter.<br>
                    <br>
                    </font><font face="Verdana" size="1"><b>Title	The Power of Cult Branding<br>
                    Author	Matthew W. Ragas and 			Bolivar J. Beuno<br>
                    Publisher	Prima Venture<br>
                    Pages	171<br>
                    Price	US$17.47, RO6.72</b></font></span></p>
                    <hr size="1">
                    <p><span style="mso-fareast-font-family: MS Mincho"><font face="Verdana" size="2"><b>Rich Dad preaches<br>
                    </b><br>
                    The book is the seventh title in the Rich Dad series. Robert T. Kiyosaki and Sharon L. Lechter, the author and co-author respectively, have written about impending financial disasters, offering remedies against insecure future financials and lessons on how to prevent the depression as well. It must be mentioned at the very outset that this book deals with an intrinsically American situation revolving around the US financial environment and government legislation. However readers living in other environments can draw corollaries to their own conditions and adapt the messages to suit them.<br>
                    The prediction of financial turmoil is based on the authors' reading of the Employee Retirement Income Security Act ratified by the US senate in 1974 during President Nixon's term and other IRA plans like 401(k). Kiyosaki's rich dad (his financial guru and father of a childhood friend) had prophesised grim financial consequences for the uninitiated. He said that these new retirement programmes, created in the U.S. and adapted by many other countries, would cause one of the biggest financial crashes in history and one day affect everybody, regardless of age or nationality.&nbsp;<br>
                    Kiyosaki prescribes eight control principles that could help investors build the financial equivalent of Noah's ark to weather the troubles ahead. These controls are proffered to reduce the dilemmas of investing and enhance the chances of retiring comfortably.<br>
                    However the book appears flawed in certain theories. For instance which way would economies be headed if, following the advice many quit their jobs to start businesses solely with the aim of selling out later for a profit? And masses get into buying houses not for living in but to make more money out of. While the prediction of impending crashes is credible, the reasoning behind the causative factors isn't entirely bullet proof!<br>
