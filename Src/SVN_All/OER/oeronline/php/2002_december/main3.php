                  <td width="100%"><font face="Verdana" size="4" color="#FF0000">Labour gains</font><p><font face="Verdana" size="1"><b>Faced with the depleting oil resource and a burgeoning population, Oman"s biggest challenge is to put in place right training systems so that the national population can respond to the dynamic labour market within and outside the country. Gus Freeman reports</b></font></p>
                    <p><img border="0" src="../../images/2002_december/48-H.E._Patricia.jpg" align="left" hspace="5" vspace="5" width="133" height="181"><font face="Verdana" size="2">Oman"s greatest resource today is 1.8 million of its own people, the second largest national population in the GCC after Saudi Arabia. Dr Fuaad Jaffer Sajwani, Vice President of the Central Bank of Oman, once told
                    OER, "The country"s natural wealth will not last forever. Human capital will be the only resource left to rely upon to continue economic growth."<br>
                    The challenges for Oman"s education and training systems in developing human resources are to produce:</font></p>
                    <ul>
                      <li>	<font face="Verdana" size="2">	quality people, in terms<br>
	of character, skills<br>
	and performance;&nbsp;</font></li>
                      <li><font face="Verdana" size="2">&nbsp;in a cost effective manner; and&nbsp;</font></li>
                      <li><font face="Verdana" size="2">&nbsp;in the sectors and skill areas&nbsp;<br>
	where there is demand.</font>
                      </li>
                    </ul>
                    <p><font face="Verdana" size="2">
                        Around the world, policy makers are looking for ways to match the supply of trained people to labour market demands. To do this effectively, education and training systems are being made more market-oriented.<br>
                        <br>
                        Patricia Sto. Tomas, Minister of Labour and Employment of the Philippines, visited Muscat recently to address the Human Resource session at the Gulf Economic Forum hosted by Ociped.  A former President of the International Labour Organisation (ILO), she shared an interesting typology of training systems from around the world and commented on their ability to adjust to changes in the labour market.  For her this is the key.<br>
                        The Minister said, "There is no ideal training system. Many variations of these systems exist and several may be in operation at the same time in one country".  The typology included the four following systems:
                        <br>
                    </font></p>
                    <ul>
                      <li><font face="Verdana" size="2">Cooperative system. For example, in
                        Germany's apprenticeship system, training is provided through government vocational schools and at the workplace, with employers paying for 50 per cent of the training. Apprentices receive half a salary. This system requires much interaction between the government, employers and trade unions and contributes to low levels of unemployment. Finance is raised from employers through a training levy of 1-2 per cent of the payroll.<br>
                        </font>
                      </li>
                      <li><font face="Verdana" size="2">Enterprise-based system - as in Japan. Employers build on the basic education system with intensive on the job training. Supervisors take on the additional role of teachers. This system produces a highly skilled workforce that is very adaptable to change.<br>
                        </font>
                      </li>
                      <li><font face="Verdana" size="2">Voluntary system - for instance in the US, individual workers add to their own human capital by returning to formal schooling (degree or further degree courses) or by taking up employer-provided training. Workers and employers decide who gets trained and in what skills.<br>
                        n	State-driven system - as in Singapore, South Korea, former Soviet Union countries, government plays the main role in imparting training. This ensures that industry had a sufficient skill base upon which to grow.<br>
                        "Private and public educational institutions have very different cultures, in my experience. Private institutions are more open to change, which is important if the system is going to meet labour market demands and     not waste scarce resources," Tomas said.<br>
                        Oman has some elements from each of these systems. However, education and training policies have been changing in recent times. The higher education sector saw the introduction of private colleges and universities in 1995. About 15 institutions exist today providing about 5,500 seats. And vocational training policy is under review within the new Ministry of Manpower established in November 2001. Therefore it remains to be seen which elements emerge in the new system.<br>
                        Tomas stressed, "Whatever the mix of elements, three factors stand out in systems that respond well to changes in labour market demand."&nbsp;</font>
                      </li>
                    </ul>
                    <p><font face="Verdana" size="2">
                        She says they are:&nbsp;</font></p>
                    <ul>
                      <li><font face="Verdana" size="2">High quality general education,&nbsp;</font></li>
                      <li><font face="Verdana" size="2">Positive returns on investment in education, and&nbsp;</font></li>
                      <li><font face="Verdana" size="2">The collaborative involvement in training systems<br>
    by employers, trainees and government.<br>
                        Oman has all these elements in varying degrees. The Ministry of Education started implementing its Basic Education Reform Programme in 1998-99 to improve quality within the general education system and give secondary school graduates vocational training.&nbsp;<br>
                        Oman also has gone ahead in forming industry specific work groups that represent partnership between government, employers and training providers to prepare school leavers for work. The formation of OPAL (the Oman Society for Petroleum Services) is one such group. (To find out more turn to page 62).<br>
                        In Tomas" opinion, such developments are encouraging as it will help Oman develop training systems that can respond quickly to the growing demands from the labour markets.
                        "The ability to change curriculum quickly in response to labour market opportunities gives the institution and the nation a competitive edge."&nbsp;</font>
                      </li>
                    </ul>
                    <p><font face="Verdana" size="2">
                        Majan College (University), the first private sector college of higher education in Oman, has recently gained approval from the Ministry of Higher Education for a new HND in ÔThe Internet and E-Business". Professor Michael Gregory, Advisor to the Board of Majan College, said,
                        "We have developed this new course in response to the changes in technology and the opportunities for employment in this field. The HND will equip people for work in Oman, the GCC and elsewhere, as companies go about putting their business processes online." He adds,
                        "We plan to develop this course to BSc level in time."<br>
                        <br>
                        Tomas cited the instance of how her own country responded quickly to the recent demand for nurses in the UK"s National Health Service.
                        "Two years ago in the Philippines we were unable to meet the demand for new nurses in the UK. Since then private sector institutions have developed new curricula to supply nurses with exactly the skill sets required. Now we are sending about 1,000 nurses per month from the Philippines to the UK."<br>
                        What is clear from the Philippines experience, where about 25 per cent of the 80 million population live and work overseas, is that the national training system is not limited to just national job market. This is of relevance to Oman, where the small job market in the country can limit the prospects of those              completing training.&nbsp;</font></p>
