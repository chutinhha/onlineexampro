                  <td width="100%"><font face="Verdana" size="4" color="#FF0000">Larger than life
                    </font>
                    <p><font face="Verdana" size="1"><b>The Chrysler Concorde 4-door LX sedan promises luxury, agility andefficiency. But does this sleek car deliver? OER finds out</b></font>
                    </p>
                    <p><font face="Verdana" size="2">First introduced in 1993, Concorde is the car that signaled the beginning of Chrysler's resurgence - a renaissance that continues today, according to the carmaker. The original Concorde introduced car buyers to the term cab forward, and became well known for the roomy interior this design affords. The completely redesigned second-generation Concorde debuted in 1998. A new version was added earlier this year, the Concorde Limited, formerly known as the LHS*. The Concorde is positioned as Chrysler's premium family sedan, featuring six-passenger seating. OER test-drives the LX base-model courtesy of Zubair Automotive priced at around RO7,650 with seven years unlimited mileage warranty with a 100,000 km free service as part of a Ramadhan promotion.<br>
                    <br>
                    <b>Walkaround<br>
                    </b>True to its inventors' intentions, the Concorde's expressive lines suggest grace and flair - the kind of agility that would be associated with a refined sport coupe.  The front fascia oozes sheet metal from top to bottom. Love it or loathe it, this car promises to excite, to move and to inspire. Some of my European friends find similarities between the Concorde and a UFO, but at least they perceive the theme of movement. Tens of thousands of Americans believe that this is the world's best looking car. The most noticeable design element to its look is its grille, edged in chrome and adorned with a big winged Chrysler medallion.&nbsp;<br>
                    <br>
                    <b>Interior<br>
                    </b>One owner in the US that has set up a fanzine dedicated to the Concorde compares the car to Sophia Loren describing it as a
                    'timeless beauty'. Time stood still for me once I popped inside the car as I struggled to grapple with the convenience of having a foot brake to unlock.&nbsp;<br>
                    There are loads of driver comfort packed inside to make the driver andco-passengers comfortable. Standard equipment includes air conditioning; power door locks with speed-sensitive auto locking feature; driver one-touch down feature windows; remote keyless entry; an 8-way power driver's seat; and intermittent wipers. There's a convenient tilt height-adjustable steering column too while the Concorde's floor console contains      rear seat AC vents, cupholders            and storage.<br>
 A rear contoured bench with trunk pass-through and fold-down armrest with cupholders. This car has classy rear passenger comfort with oodles       of space for family members with lanky legs.&nbsp;<br>
                    The trunk looks as if it could fit a small car although in truth it offers a whopping 18.7 cubic feet of cargo space. The greatest feature is that it offers you the chance to squeeze two sets of large suitcases on top of each other inside and still have room to squeeze your hand luggage.&nbsp;<br>
                    <br>
                    <b>Performance<br>
                    </b>The Chrysler Concorde delivers a smooth ride, brisk acceleration performance, and impressive handling. This car handles rough roads along the beach well.&nbsp;<br>
                    The suspension filters unwanted vibration and noise is kept low. The LX has a 2.7-litre V6 engine which delivers 200 horsepower power at 5,800 rpm. The engine is teamed with a 4-speed automatic transmission.  At high speeds, the Concorde feels extremely stable. Its direct, precise steering does rate among the best in its class.&nbsp;<br>
                    The chassis grips impressively in hard cornering and the brakes deliver a solid, stable performance that is amazing given its size. It's easy and fun to drive on winding roads.&nbsp;<br>
                    <br>
                    <b>Safety</b><br>
                    The Concorde LX comes with air bags for driver and front passenger,        four-wheel
                    16' disc brakes and brake-park interlock. Dynamic side-impact intrusion protection and head injury protection are also present, claims Chrysler. The power door locks with speed-sensitive auto locking      feature and rear door child                      protection locks.&nbsp;<br>
                    Meanwhile, another standard feature is the delay of your         headlights switching off allowing you to park and walk joyfully to your door with light shed on this pedestrian journey.<br>
                    The Concorde with its robust exterior, practical interior, solid acceleration and excellent handling, is going to appeal towards a middle-aged manager who wants to obtain that feeling that they have
                    'arrived in life'.  To a certain extent, they will if they make a home for this Chrysler. With generous legroom at the rear of the vehicle, it is obvious that it will do well with those of you who already have teenagers.  Personally, the Concorde's Limited model* appeals more as it has many more features and comes with a leather finish inside rather than a cloth interior but given the sharp price for the size of car and its features, the Concorde is primed to obtain more owners in Oman.&nbsp;
                    </font></p>
