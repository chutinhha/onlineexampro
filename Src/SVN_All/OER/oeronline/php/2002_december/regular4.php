                  <td width="100%"><font face="Verdana" size="4" color="#FF0000">Silk kites and brand extensions</font><p><b><font face="Verdana" size="1">With garment producers making kites, it is time to re-visit the concept of diversification</font></b><font face="Verdana" size="2"><br>
                    <br>
                    </font><span style="mso-fareast-font-family: MS Mincho"><font face="Verdana" size="2">On page 24 of a recent weekend magazine of a newspaper lay a surprise for my senses. Not only for the physical ones but also for the professional one that would normally not balk at marketing oddities. I could not fathom the logic behind venerable high fashion establishments like Versace, Escada, or for that matter
                    Herms, getting into the manufacturing of kites. Yes, those objects made of thin sticks and cheap paper, that kids all over the world fly with some string. The difference, however, lay in the fact that these were made of beautifully patterned silk. And the cheapest of these kites was priced over US$200!&nbsp;<br>
                    I thought I could see some convoluted reasoning behind
                    Herms' actions. They are well-known for their unaffordable silk scarves...had some old surplus material...and the marketing executive had the bright idea to make them into kites. Smooth! But Escada? Or Versace? Well maybe they too had some surplus silk lying around, or did some sophisticated research tell them that this season kites would be the craze as accessories? May be it was meant as a placebo to the sinking stock markets worldwide! The mind fumbles. All it can do is to predict the season's high society party gossip.&nbsp;<br>
                    <br>
                    Fashion designers into kites? Why not? Calvin Klein went into perfumes. Chanel has gone into watches. Caterpillar (the heavy engineering company) walked into the
                    'tough footwear' category. Louis Vuitton, the famed luggage designer, has forayed into watches. Not the diamond-heavy ones but sleek
                    'rough' ones with sewn leather bands. The brand logic here: durable great-looking luggage for exotic journeys needs a smart tough watch.<br>
                    <br>
                    In a similar way, Caterpillar extended their reputation for building huge earth-moving equipment and other monstrosities to making boots with steel caps. They managed to successfully transfer their brand-essence of
                    'tough and enduring' to these footwear for the rough and the tough. Now they have started normal casual shoes too. I don't think people want that from them. Why would one buy a Hush Puppy from Caterpillar? Mistake, I should think. Despite Messrs Ries and Trout's ranting and raving against Ôline extensions', in their legendary best-seller Positioning, marketers still seem to be fiddling around with it after 20 years and I dare say some have been actually been quite successful.<br>
                    <br>
                    In sheer defiance of Ries and Trout and the sensibilities of the marketing world, a group of companies in Japan actually went about and formed a brand banner under which they successfully marketed a weird combination of products from clothes to gizmos. And went on to achieve cult status. In India, brand extensions from the initial product category have been widely successful. An example is Godrej, one of the country's top business houses. In the Sixties, Godrej meant
                    'security', as in impossible to break locks and steel cupboards where one could safely stash away the jewellery. In later years, they capitalised on their formidable reputation of
                    'strength and durability' and expanded into metal furniture, office storage and so on. That was classic diversification as promulgated by Ries and Trout.<br>
                    <br>
                    But what is Godrej into today? Take a look - insecticides, personal care, agriculture and foods, real estate, technology, home appliances, and so on - and that is not even the complete list! And in each market or industry they have been pretty successful. So what is the common thread here? To me it's the sheer credibility of a company that has been around forever and never fallen back on its promises. This transition from a strong security products brand to a unified corporate brand did not happen in a day - but it worked.<br>
                    <br>
                    But still, Ries and Trout are right when they say that most extension efforts do not click. They give sordid accounts of expensive failures. Xerox computers, Kodak copiers, Levi's for the feet, and their reasoning makes a lot of sense. Line extensions have to be managed carefully and not be just seen as a penny saving strategy. Sometimes riding on a successful brand helps and sometimes not.&nbsp;<br>
                    <br>
                    Like it is often said, it is all in the consumer's mind. And it is quite slippery and dark in there! But, whether it be clever reasoning or foolish ploy, fancy flights of silk kites are rather unusual cases of brand
                    extensions.</font></span></p>
