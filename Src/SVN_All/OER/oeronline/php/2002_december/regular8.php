                  <td width="100%"><font face="Verdana" size="4" color="#FF0000">At
                    the helm</font><p><span style="mso-fareast-font-family: MS Mincho"><font face="Verdana" size="2"><b>Banking on experience<br>
                    </b><br>
                    <img border="0" src="../../images/2002_december/82-helm.jpg" align="left" hspace="5" vspace="5"></font><b><font face="Verdana" size="1">Robert Bray, CEO, HSBC Bank Middle East, Oman, is a tough talking executive.
                    Nayl D'Souza engages him in a revealing conversation</font></b><font face="Verdana" size="2"><br>
                    <br>
                    <b>Career with HSBC&nbsp;</b><br>
                    This is my third occasion of working in Oman for HSBC. I first served here in 1974 for two years and returned in 1979 for three years.&nbsp;<br>
                    I have served almost 18 years of my career in the Middle East with interesting postings in the UAE, Jordan, Yemen, Saudi Arabia and for a brief period in Egypt. My job in Saudi Arabia came at a time of change and therefore was a particular challenge. I was in Jeddah for two years at the time the British Bank of the Middle East changed its name and company structure to become The Saudi British Bank. Prior to that, in 1972, I was in Dubai in charge of the cash department when the country launched the UAE Dirham. I spent 10 years in various roles in Hong Kong and three years as Senior Manager Credit for India.<br>
                    <br>
                    <b>Worst mistakes</b><br>
                    Any bank executive has to manage risk. Our decisions are based on the assessment of the business case and the reliability of the client. We do make mistakes, of course, but that is the nature of the business. You have done all the research needed but the loan still goes bad over time. I am proud to say that I have not made any seriously bad mistakes,                but I would be a magician if I had managed to avoid all            the pitfalls.<br>
                    <br>
                    <b>Lending and learning</b><br>
                    Over the years I have learnt to become a good judge of character with an ability to assess people quite quickly after first introductions. How do they look, how do they dress, how do they interact with people, how do they behave in situations like playing golf, at a business lunch, or when they come to me to ask for a loan!<br>
                    <br>
                    <b>Experience counts</b><br>
                    A few grey hair will help. You can hire a qualified young graduate with proven acumen and intelligence but you still need experience of years in the job. A lot of youngsters do not have the maturity of a seasoned decision-maker. A combination of experience and youthful dynamism is ideal.<br>
                    <br>
                    <b>Active role</b><br>
                    My job at HSBC Bank Middle East is very hands-on. I am not the kind of CEO who sits at a desk and just rubber-stamping. A receptive boss will be able to stay in touch with most aspects of the business. When people bring their problems to me, I delve deeper into their worries. This helps my staff to understand that I am not aloof from their concerns.<br>
                    <br>
                    <b>Omanisation: an achievement and a challenge</b><br>
                    HSBC Bank Middle East in the Sultanate has achieved an Omanisation level of 90 per cent from 25 per cent in a few years. This has been a remarkable achievement, but the speed of the transformation has brought some challenges too. We have lost some continuity, our experience levels have diminished, and we have had to work hard to keep our service levels at a consistently high level. We are devoting a lot of time and resources on hands-on training to manage this challenge.<br>
                    <br>
                    <b>Credit crunch</b><br>
                    There is no doubt that some banks in the Gulf do push credit cards aggressively to stay competitive in the local market. We have a full range of products. We are not bankers to all people. We do not want our customers to take services they do not need or cannot afford. We only lend in excess of 30 times salary and not 40 times, as most banks do in Oman. This may not make us the most attractive bank to some customers, but to counter this we offer value that few of our competitors can match. Our credit cards for instance are called the heavyweight champion of cards in Oman specifically because of the features and benefits they offer.<br>
                    <br>
                    <b>Entrepreneurial spirit</b><br>
                    There are many extremely talented local business people in the market. The problem is that banks have lost money in the last few years on small companies because many of them have gone out of business. Oman's banking industry is more cautious and cannot afford to keep getting burnt.&nbsp;<br>
                    <br>
                    <b>Personal weaknesses&nbsp;</b><br>
                    I'm probably too tough. Sometimes you are sent to do a job that is tough and you are not going to win friends. Maybe at times, I'm a little bit too dogmatic too. This could be considered a requirement in my profession.<br>
                    I probably do not delegate functions as much as I should. I think many of us have to learn to delegate more. You have got to develop trust that your team is going to do a good job. Sometimes I worry that the work I have delegated is not going to be handled perfectly. Perhaps I should simply learn to relax more and let my team get on with it.&nbsp;<br>
                    <br>
                    <b>Leadership style</b><br>
                    I have shown leadership qualities right from my youth. I make sure that I have a good team around me, who can make up for my weaknesses. I am a perfectionist but I am not very good at crossing my
                    't's and dotting my 'i's, so I surround myself with people who can do that. I believe in asking the team their views. But at the end of the day, the final decision is mine.<br>
                    Trade secrets<br>
                    In my life I have always been prepared to do any job that I have asked a member of my staff to do. During my career, I have on some occasions rolled up my sleeves, mopped floors, and there is nothing I will not do. So if my staff tell me with their pride,
                    "Oh boss, we are not going to do that." Then I will reply, "Well if you are not going to do that, I will." If the boss is prepared to sit down with the staff, balance the books and share the load, he or she earns respect.&nbsp;<br>
                    There is no point a General jumping out of the trenches shouting
                    'Follow me' if nobody follows. I have always believed that if I said
                    "follow me out of the trenches", a lot of my staff would. That is what I consider success. They trust me. They might not like me because a lot of my decisions have been tough and unpopular but they trust and respect me and that's what counts.<br>
                    <br>
                    <b>Future strategy&nbsp;</b><br>
                    HSBC will continue to diversify its banking business locally, and selectively increase lending to the large and professionally managed companies in Oman. Partly to reflect the Group's commitment to wealth management of its customers, and partly in view of the low interest rates, we are focusing on value-added service and products for our customers or non-funds income as we call it. Project financing is also another important facet of our Bank, which involves a specialised arm based in Dubai as these projects usually require loans from a syndicate of banks. Next year, HSBC in Oman has plans to introduce a new personal internet banking service which has already been launched around the world and is being rolled out in the Middle East.<br>
                    </font></span></p>
