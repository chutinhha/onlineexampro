                  <td width="100%"><font face="Verdana" size="4" color="#FF0000">The silicon power</font><p><b><font face="Verdana" size="1">If your home PC is becoming a supercomputer, blame it on a tiny piece of silicon. Microprocessors, which power computers, are  gathering strength by the hour, offering never-before computing experience</font></b><font face="Verdana" size="2"><br>
                    <br>
                    </font><span style="mso-fareast-font-family: MS Mincho"><font face="Verdana" size="2">When I decided to buy a Pentium III 500 Mhz desktop computer in 1999, it was the most powerful home PC around. Many of my tech-savvy friends told me I was mis-spending my money. They said I could well make do with a low-powered machine, since I would be using the PC mainly for net surfing and writing.<br>
                    Just three year's down the line, and I find my PC, (yes, I bought that P III), to be wanting in power. Today I use my PC to listen to music, watch films, chat face-to-face with friends across the globe and learn tips on playing Spanish guitar. Apart from these leisure activities, I also do banking, stock trading, paying bills and a bit of e-shopping. Surely I need more power, and this time around, none of my pals is arguing. But I am wondering whether to spend on a new PC now or wait a bit more. Every six months or so, a new microprocessor or its latest version arrives to add more power to the PC and make its predecessor obsolete.&nbsp;<br>
                    Recently, Intel Corporation, the giant of a chipmaker, launched its 3.06 Ghz version of the Pentium 4. This new processor, incorporating a technology called `hyper
                    threading', lets a computer do two things at the same time, without compromising on power. This means that you can run a game and download a movie simultaneously, both happening smoothly, faster than ever before.<br>
                    Compared to my old Pentium III, this one can run games eight times faster; video encoding seven times quicker and play digital music 8.6 times faster. The hyper threading technology would allow it to do multitasking such as running a virus scan, power point and Acrobat together 20 times better than before. For instance, it will take just 12 minutes to encode a 20-minute digital video and six CDs of music together on a PC powered by the new chip vis-ˆ-vis three hours on a P III machine.&nbsp;<br>
                    Nobody, not even many Intel officials, believed that such a powerful chip would come anytime soon. But microprocessors are gathering strength by the day.
                    Intel's co-founder Gordon Moore had predicted in 1965 that the number of transistors on chip would double in every 18 months. This forecast, known as
                    Moore's Law now, has held good till date and it is believed that it would be relevant even in 2010. While we are at 3.06 Ghz now, we will have 30 GHz chips then, containing one billion transistors in a single piece of silicon!&nbsp;<br>
                    Such mind-boggling computing power also throws up some serious issues. As the power goes up, so do the heat and noise inside the PC. Chipmakers are now striving to find means to address this issue. Intel is developing a micro-radiator, built into the silicon to cool the processor. Also, search for a substitute to silicon - the fundamental material at the heart of the processor - is now on.&nbsp;<br>
                    History tells us that it was indeed advances in microprocessors that drove new applications and made computers user-friendlier. Bill Gates and Paul Allen, founders of Microsoft, realised this when they were teenagers. When they saw a new chip called
                    '8008' by Intel in 1972, Gates and Allen foresaw that future of computing would be built around such processors. That realisation was in fact the cornerstone of everything they did together later, including the founding of Microsoft.<br>
                    Besides Intel, its arch-rival and the number two chipmaker AMD, too, is readying a new 64-bit microprocessor - like the ones now being used in servers for high-end computing - for the desktops. To be called AMD Athlon 64, this new chip can run both 32-bit as well as 64-bit software applications and is expected to offer some tough competition to
                    Intel's bread and butter Pentium. Earlier codenamed 'Clawhammer', this new processor would provide, claims AMD, vivid images and richer sound for digital entertainment.<br>
                    Nevertheless, every time a new chip is released, sceptics ask this question: Why push such potent stuff down our throat when we are happy with existing processors? I
                    don't think anybody would ask a carmaker why a new, better and powerful car is being launched when there are so many of them around. At least in the case of microprocessors, price is something that always drops by the day. So why complain?&nbsp;</font>
                    <br>
                    </span></p>
