                  <td width="100%"><font face="Verdana" size="4" color="#FF0000">Counting on the census</font><p><font face="Verdana" size="1"><b>Businesses and policy makers in the Sultanate are gearing up for new statistics on the population as well as cyber docking with the government. Gus Freeman reports</b></font><font face="Verdana" size="2"><br>
                    <br>
                    <img border="0" src="../../images/2002_december/34-popu.jpg" align="left" hspace="5" vspace="5" width="175" height="121">The 1993 census data has become dated due to the breathtaking pace of change in Oman. For instance, in 1993 there were only a few hundred households in Al Mawaalih in Muscat. Today it has two large retail complexes - Muscat City Centre and Markaz Al Bahja - and a densely populated residential area.<br>
                    The first set of figures for the 2003 census, is planned for release on December 20, 2003. An even more exciting date will be March 19, 2004 - when the government expects to publish the full results online through the website of the Ministry of National Economy. Since the value of information is greater if you use it sooner, there will be many in waiting to capitalise on the data as soon as it is released.<br>
                    Ali Mahboob, Director General, Social Statistics at the Ministry of National Economy and in charge of the 2003 census, told OER, "During the first census it took nine months from the end of data collection to publishing the full set of tables. This time we intend to reduce this lag to just four months. We plan to use hand-held data entry devices for enumerators in the Muscat Capital Area. With about 25 per cent of the (national) population residing in Muscat, this should accelerate the process. If we use them, Oman will be the first country in the world to deploy hi-tech personal digital assistants (PDAs) on a census project," he adds. The UN will provide US$50,000 to help finance the exercise.<br>
                    Describing the usefulness of the census, Mahboob said, "Businesses will know about the size of the population, its geographical distribution, age characteristics, educational levels and social status, among other things."&nbsp;<br>
                    "If I am manufacturing clothing for ladies, I need to know the age-wise distribution of the female population. To set up a supermarket, I need to know where the high population densities are. I cannot plan without knowing the market." The data will reveal how Oman’s society and particular market segments have changed. The trends will enable better predictions of future growth. Businesses operating in GCC markets will be able to compare the markets in Oman and other countries, and adjust plans and strategies accordingly. Mahboob noted, "The utilisation of the last census by government and private sector has been a fraction of the potential. People tend to concentrate just on the population data, but there is much more."<br>
                    The ministry has interacted with all government departments and the Oman Chamber of Commerce and Industry, representing the private sector, to find out what they require of the census. D. Swaminathan, Senior General Manager, Business Development at Oman Trading Establishment, says, "We are already using the ministry’s statistical yearbooks and shall be interested in the census. It will be useful in our business modelling and market studies." Sadiq Mohammed A Khaliq, Senior Manager, Investment Banking, at BankMuscat, reveals, "We use the monthly statistical bulletin when analysing companies and will use the census results to examine the markets upon which depend the performance of our focus companies." Oman Airports Management Company told OER that they would use the data to plan how best to serve the domestic travel market.<br>
                    <br>
                    <b>Timely dissemination&nbsp;</b><br>
                    Swaminathan felt that the data will be especially useful if the delay from collecting data to publishing can be within four months." Ali Mahboob revealed an impressive timetable, "Census night falls on midnight of December 7-8, 2003. Data collection will continue till December 17. In 1993 we took 10 days to produce the first quick count. We aim to reduce this to just one day.&nbsp;<br>
                    <br>
                    <b>Output formats</b><br>
                    The 1993 census results are available in print only and the ministry supplies any special tables on request. In 2004, census tables will become accessible through a website. The data for each census block, which average 60 households, will be produced on a CD (Nizwa Town had just over 90 census blocks in 1993). This will give people the flexibility to cross-tabulate variables, as well as produce their own tables from census-block to the nation levels. The Ministry plans     to link it to a Geographical Information System, which can be loaded from the same CD or downloaded from the website. Swaminathan commented, "Census data online would be even more user friendly than on a CD."<br>
                    The main variables of the census remain the same as the previous one (See box on the left). "We do not want to change the questionnaire too much, as then we cannot make comparisons between 1993 and 2003," said Mahboob. The new feature will be the census block profiles about the        physical infrastructure of each locality in terms of:</font></p>
                    <ul>
                      <li><font face="Verdana" size="2">GPS position</font></li>
                      <li><font face="Verdana" size="2">Road conditions</font></li>
                      <li><font face="Verdana" size="2">Services are available, such as gas 		station, school, hospital, clinic, 			police station, electricity, water 			and telephone</font></li>
                      <li><font face="Verdana" size="2">Number of dwellings&nbsp;</font></li>
                    </ul>
                    <p><font face="Verdana" size="2">Mahboob said, "A significant proportion of Oman’s population lies scattered in small clusters of less than 50 households. The profiling will show exactly where the people are and what services they have, and it can be linked to the social, demographic and population data, providing a rich tool for businesses." The census plays a vital role in the planning of the household expenditure and income surveys and employment surveys.&nbsp;<br>
                    In fact, it will help in the planning of the next census too, which will take place in 2010 at the same time as in all the other GCC nations. "I represent Oman on the GCC Census Coordination Committee, which held its first meeting last month. We seek to standardise questionnaires, definitions and methodologies so that data between countries can be compared and added up in 2010," informed Mahboob.&nbsp;<br>
                    When the enumerators visit you between December 8 and 17, do cooperate with them. In the meantime, it may be wise to begin thinking about how you will use the wealth of information.&nbsp;</font></p>
