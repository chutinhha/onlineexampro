                  <td width="100%"><font face="Verdana" size="4" color="#FF0000">Southward ho!</font><p><font face="Verdana" size="2"><b>Despite a massive military build up in Gulf, the oil prices have maintained a relative stability. For any shortage is likely to be met with the release of the US' strategic oil reserves and the increasing non-OPEC oil supply</b><br>
                    <br>
                    </font><span style="mso-fareast-font-family: MS Mincho"><font face="Verdana" size="2">The threat of war in the Middle East traditionally sends oil prices through the roof. Yet in October, as the US troops amassed in the Gulf preparing for a full-scale attack on Iraq, the price of crude plunged by some US$4 per barrel to around US$24 per barrel. This unexpected dip has thrown up two key questions: First, what forces dragged prices down, at a time when history suggests they should have been soaring? Second, and more important, what does this tell us about the direction of prices in the coming months?<br>
                    As always, the first question is by far easier to answer. Two factors underpinned the price fall, and both are on the supply-side. OPEC quota-busting reached new heights during October, reaching around 2.7 million barrels per day (mbd). That's more than 10 per cent above the cartel's self-imposed quota of 21.7 mbd. More important, it represents more than three per cent of average global consumption (around 78 mbd) - a significant amount for the world's highly elastic oil markets. The Energy Information Administration (EIA), part of the US Government's Department of Energy, acknowledged that
                    'the prospect of increasing supplies in the near future' - as the OPEC 10 continued to increase production above quota levels - has played a vital role in sending prices downwards.</font></span></p>
                    <p><span style="mso-fareast-font-family: MS Mincho"><font face="Verdana" size="2">
                    Perhaps more important, oil traders sensed an improvement in the diplomatic climate, as Iraq showed signs of bowing to US and UN pressure over its weapons programme. Traders bet that the prospect of war - and with it the threat of any disturbance in Middle East oil supply - had fallen. The price of oil futures swiftly followed suit.&nbsp;<br>
                    'In October, crude oil markets were dominated by discussions about the US draft resolution at the United Nations Security Council,' wrote OPEC in its November Oil Market Bulletin.
                    'In other words, crude prices responded to developments in the political arena, neglecting the underlying market fundamentals.' The EIA agreed in its Short Term Energy Outlook,
                    'Hope for a diplomatic solution in the UN's confrontation with Iraq eased tensions.'</font></span></p>
                    <p><span style="mso-fareast-font-family: MS Mincho"><font face="Verdana" size="2">
                    Prices closed at around US$24 per barrel in October, and fluctuated around that level during the first half of November.
                    'The crude market had another volatile week, thanks to the Iraq situation, but staged a modest recovery by the close of November 15, giving some hope that prices have found some short-term support,' wrote Standard Bank Analysts William Buchanan and Don Jameson last fortnight.</font></span></p>
                    <p><span style="mso-fareast-font-family: MS Mincho"><font face="Verdana" size="2">
                    This leads to the second, more pressing question: which way is oil prices headed in the coming months? Surprisingly, most analysts think they are more likely to go down than upwards. Standard Chartered Bank forecasts an oil price of US$20-US$22 per barrel in 2003, broadly in line with the market consensus. At first glance, such a bearish prediction appears strange - during the last major Gulf crisis in 1990-91, prices reached the equivalent of more than US$50 per barrel.</font></span></p>
                    <p><span style="mso-fareast-font-family: MS Mincho"><font face="Verdana" size="2">
                    However, a host of new factors support the market's cautious outlook. Supply is surging from OPEC and non-OPEC producers, while demand remains weak. Analysts do not expect Kuwaiti production to be cut this time, as it was a decade ago; and the oil world is better prepared for war in Iraq, having learned the lessons of the previous campaign.<br>
                    First, supply. 'At higher production levels, market fundamentals will reduce upward pressures on prices,' says the EIA. More specifically, demand is forecast to rise by 1.3 mbd next year on the back of a modest economic recovery in the US, the world's biggest oil consumer. This is better than 2002, but not enough to soak up all the extra barrels set to enter circulation.&nbsp;</font></span></p>
                    <p><span style="mso-fareast-font-family: MS Mincho"><font face="Verdana" size="2">
                    'Rising non-OPEC production is expected to meet much of this new demand. Additional oil exports from Russia and the Caspian Sea region are expected to provide about half of the anticipated one mbd increase in non-OPEC supply in 2003,' says the EIA. Increases are also expected to come from offshore Africa, Mexico and Canada, leaving little or no room for OPEC supply increases. Given that the cartel's quota discipline is at rock bottom, it seems likely that members (including Kuwait) will over-supply the market, forcing prices southwards.</font></span></p>
                    <p align="center"><img border="0" src="../../images/2002_december/54-oil-gas.jpg" width="300" height="156"><br>
                    <span style="mso-fareast-font-family: MS Mincho"><font face="Verdana" size="1">Oil
                    trends</font></span></p>
                    <p><span style="mso-fareast-font-family: MS Mincho"><font face="Verdana" size="2">
                    Second is preparation. 'There are a lot of differences this time around,' says Richard Gilhooly, economist at French investment bank BNP Paribas.
                    'Most importantly, the planning and preparation appears to have [taken place].' In particular, the US emergency oil reserve has reached the highest level in its 25-year history. In 1991, President George Bush Sr. failed to make full use of his country's crude stockpile, an error that most analysts agree sent panic through the markets. His son is unlikely to make the same mistake - the Strategic Petroleum Reserve now stands at 592 million barrels, enough to replace Iraqi and Kuwaiti daily production for up to five months.
                    'At a time when America's energy security is one of our highest national priorities, this milestone is especially timely,' says US Energy Secretary Spencer Abraham.</font></span></p>
                    <p><span style="mso-fareast-font-family: MS Mincho"><font face="Verdana" size="2">
                    For all these reasons, it seems reasonable to assume that prices will remain on a relatively even keel. OPEC is happy with crude selling for just above US$20 per barrel, as are the world's major consumers. Of course, it will be a very different story if war in Iraq spills over into Kuwait or even Saudi Arabia - that would send prices soaring. Similarly, a deep recession in the US would see oil demand drying up, sending prices through the floor. But if these extreme scenarios can be avoided, as US policymakers hope, a period of relative oil price stability could emerge from the political turmoil as 2003 unfolds.</font>
                    </span></p>
