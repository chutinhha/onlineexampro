                  <td width="100%"><font face="Verdana" size="4" color="#FF0000">Profile

                    - </font><font face="Verdana" size="4" color="#FF0000">Building success</font><p><font face="Verdana" size="1"><b>OER profiles Krishnakumar Taori, the mastermind who manages the day-to-day operations of Hasan Juma Backer group of companies, as we talk to him about the recent expansion and planned growth of the  business for next year</b></font></p>

                    <p><font face="Verdana" size="2"><img border="0" src="../../images/2002_december/44-profile.jpg" align="left" hspace="5" vspace="5" width="184" height="216">Hasan Juma Backer Trading and Contracting Co. (HJB) LLC has taken major strides in its recent past but more changes are planned. Krishnakumar Taori gained promotion as Group Managing Director from General Manager last month with the blessing of the Chairman Hasan Juma Backer after 13 years of service. He confirms that the changes to the group's management structure also included making Ahmed Hasan Juma, the Chairman's son, vice-chairman of the group. He says the changes are necessary to adequately address the management challenges arising out of rapid expansion of the group's activities. It has steadily grown to become a major player in the local construction industry with a turnover last year of around RO12 million (US$31.2 million). Taori, who expects the company to register a turnover closer to RO20 million (US$52 million) by the end of 2002, says he has now been given the added responsibility of ensuring the company succeeds in a highly ambitious expansion plan which involves more diversification next year.&nbsp;<br>

                    The company initially built villas and although property construction is still a key component of its trade, HJB has broadened out into a diverse range of activities from infrastructure (water supply and road building among other tasks) to tourism, and from marble supply to the assembly of switchgear products.<br>

                    Taori joined the company in 1989, some 15 years after the company was founded. Following his appointment, the growth of the company measured by staffing numbers indicates the success that has been achieved. In 1989, less than 200 staff were employed but today that figure is over 1,500 despite the fact that the company is moving towards less labour intensive construction like roads rather than housing - a move that is enabling the company to boost its Omanisation in skilled jobs.<br>

                    Taori leads comfortably. His colleagues find him approachable and polite. Staff at his company show him respect rather than fear. The stability of personnel tenure is a clear sign that he treats employees with kindness and justice. He seems reasonably timid although his power to exact obedience surfaces with intelligent and patient timing. "I'm not a good teacher," he confesses. "But I've learnt to control my temper and that gift has helped me progress my career."<br>

                    <br>

                    <b>Roads and water distribution</b><br>

                    The company is involved in road construction in many places across the country, most of which are located around Sohar, Suwaiq and Nizwa. It has also won a four-year annual road maintenance contract for parts of the Batinah and Mussandam region among other road projects.&nbsp;<br>

                    In the water supply industry, the trading group has been chosen as contractors for the Al Massarat Water Distribution Project (winning a contract worth RO 1.6 million), Bausher Water Supply Project (RO 2.0 million), and Al Sharqiya water supply project (about RO 3.5 million). The company has also been awarded parts of the Barka Water Supply Project for RO 5.0 million and work should progress on this in December which will involve constructing about 270 km of pipelines and building several reservoirs and pumping stations.<br>

                    <br>

                    <b>Timely completion of projects</b><br>

                    One of the classic examples of timely work which has built up the company's reputation in the local market was the construction of the Al Felaij Castle Theatre at a cost of RO800,000. Against an impossible odd of completing the project within 90 days, the company completed construction with five days to spare. "Ninety days was a very short time for Al Felaij Castle Theatre to be completed. The intricacies of the work included constructing a Royal Box and installing state-of-the-art lighting and audio equipment for the theatre," he says.<br>

                    <br>

                    <b>Urich Plant LLC</b><br>

                    The Hasan Juma Backer Group faces many challenges but chief among these is the lack of professionalism shown by subcontractors and third party service companies that it relies upon to complete its projects.  As a result, the company has diversified into areas that complement its trade to allow less dependency on such companies and this, in turn, has increased the competitiveness of the construction arm of the business, according to Taori.<br>

                    "We try to manage projects among our companies and this has been a success for the fact we play a larger role in determining our project's fate and thus avoiding dependency on service providers who are not properly organised, professionally managed or adequately funded, " he says.&nbsp;<br>

                    The newly established Urich Plant LLC, the fastest growing company in the group, has been founded with a view to hiring out plant and machinery to its construction division, considerably augmenting the group's inventory, and assisting in the mobilisation of equipment and machinery to locations in the interior of Oman. The company will soon begin work on constructing an asphalt plant to help it on road construction and maintenance projects.<br>

                    "Since last October, we've had exponential growth in construction equipment," adds Taori who estimates that the company has spent more than RO4.0 million on new machinery this year.<br>

                    <br>

                    <b>Other integration</b><br>

                    Similarly, the business has formed a subsidiary called Urich Switchgear Manufacturing LLC which involves the design, engineering and assembly of low voltage switchgear products and is capable of supplying electrical switchboards which are used in power generation, distribution, control, protection and measurement. The switchgear components are being sourced from Korea, Japan and Germany. The company has recently identified Sudan as an export target for the products once assembled locally. In 2003, the company will aim to boost its export quantity to such markets.<br>

                    Another component of the trading house is Urich Tours, Travel &amp; Cargo LLC which aims to become a leading travel agent in Oman offering a comprehensive package of travel care. Currently, the company provides only six per cent of total turnover for the group, but in 2003 Taori plans to raise its contribution through aggressive marketing to segments like value added travel, shipping and freight forwarding as well as managed tours.&nbsp;<br>

                    <br>

                    <b>Recipe for success</b><br>

                    The key executives of the company have been working with Taori for the past decade. He admits that this fact has helped considerably in achieving the progress that has been made, saying, "We work as a team of committed professionals with a common goal of growth. Many of our senior key managers have been working along side me for 10 years or more which is a great advantage of our organisation."&nbsp;<br>

                    "The owner has given me unstilted support to my efforts to remodel and rejuvenate the organisation through the years. With his continued belief and encouragement, we will continue to do well."<br>

                    The future looks bright. The company has already filled its order books full of projects for the first six months of next year. Typically, by December only a third of the year's trading activity would be accounted for.&nbsp;<br>

                    "The reason why we've managed to succeed is because we've been able to develop our human resources and keep them motivated as well as the fact that our company is employing sound engineering practices, modern management techniques and a dedicated team of professionals," adds Taori. The company runs a health club and invested RO150,000 to build a weekend resort at Sawadi Beach a few years ago for its personnel.<br>

                    <br>

                    <b>Commitment to quality&nbsp;</b><br>

                    Two years ago, Hasan Juma Backer Trading received ISO 9002: 1994 certification even though Taori says the company was not forced to try for accreditation. The company does have growing ambitions to work in the oil and gas sector where possessing such an award is becoming a requirement. "We felt that our customer focus was so strong that we could easily put the management system into place to allow us to gain future growth," Taori says.<br>

