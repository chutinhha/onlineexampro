            <font face="Verdana" size="4" color="#FF0000">The future of work <br>
            </font><font face="Verdana" size="1"><br>
            <b>How will tomorrow’s companies need to change to attract and 
            retain the next generation of talent? Alan Strutt, editor, MCE, 
            summarises a presentation by Dr Karen Moloney, MCE faculty member<br>
            </b><br>
            <img border="0" src="im28.jpg" align="left" hspace="5" width="250" height="204">Our 
            leaders are drawn from a privileged pool of educated, secure, 
            confident workers, some of whom may not have to work for an income! 
            We compete for them, not just with other employers, but with the 
            luxury of staying home, running a vineyard, and working when and 
            where they want – should the offer inspire them. We drag them back 
            out of retirement because they are bored, or we attract them into 
            our corporations from insecure dot.coms or start-up businesses only 
            to find they are off again once they have had another idea. Are we 
            able to attract and keep these kinds of people?<br>
            Furthermore, do we really understand what workplaces need to provide 
            for new generations in the aftermath of Enron and Parmalat? Are we 
            prepared to be transparent about our dealings, or will we just share 
            the legal minimum required? Are we really committed to listening to 
            our social conscience, or is it just talk for the sake of the 
            regulatory bodies?<br>
            Do we want to offer work-life balance, or should we just accept that 
            we are the kind of organisation that loves and needs workaholics? 
            Given that more women than ever before have entered the workforce, 
            should we strive for equity, or accept that women’s and men’s needs 
            and wishes may be different? What about older workers? Many 
            organisations are finding the older worker more committed, loyal and 
            reliable – but in some countries, ageism is still rife.<br>
            These are the kinds of questions that employers all across are 
            asking in order to prepare themselves for the next generation of 
            employees. As society changes, so the workplace must change.<br>
            <br>
            The presentation<br>
            Dr Karen Moloney, MCE faculty member, looked at how tomorrow’s 
            companies would attract and retain the next generation of talent. 
            And in order to do this, we need to understand what different 
            generations want.<br>
            Older workers (born pre-WWII), have primarily worked for one 
            company, had one career, and have been used to limited travel and 
            post-war austerity. Women of this age mostly stayed at home. They 
            are a group that can offer experience, loyalty and reliability. They 
            would like a choice in where and when they work, want respect and 
            participation, and may be working ‘to give something back’. <br>
            Baby boomers (born 1945-65) have experienced job insecurity, 
            increased competition, and have seen women truly enter the 
            workforce.<br>
            They enjoy collaboration and teamwork, and want affluence, 
            employability, and financial rewards. They may have had several 
            careers, may need retraining.<br>
            Generation X (born 1965-77), may well have seen both their parents 
            work, are the ‘latch-key’ generation, and have seen the birth of the 
            Internet – they are technically literate. They want quality of life, 
            reject the ‘cube’ farm, need a challenge and flexible working, and 
            enjoy autonomy.<br>
            Generation Y (born 1977-2000) have seen corporations and the dot.com 
            bubble collapse, as well as the globalisation of commerce, crime and 
            terrorism, and global warming. They take technology for granted, are 
            a networked generation, care about ethics and the environment and 
            are international.<br>
            <br>
            A vision of 2020<br>
            Karen presented a vision of 2020—in terms of the environment, 
            technology, economics, health, government, society and 
            demographics—and the implications of these for HR. <br>
            The environment will have more genetically-modified crops grown than 
            natural crops, and more fish farmed than fished. Global warming will 
            continue to wipe out species, floating homes will offer safety in 
            flood-torn regions (for example, Amsterdam and Venice), and 
            earthquakes will kill more people as cities grow. There will be 
            chronic water shortages as demand increases, and the ‘three worlds’ 
            theory will happen – we will need three planets to sustain us if the 
            whole planet becomes as affluent as the US, Canada and Western 
            Europe. <br>
            Implication: For HR, this would imply carefully considering where 
            you work globally, and ensuring it has reliable sources of energy 
            and water. <br>
            In terms of technology, we will see solar cells in fabrics, personal 
            power chips in clothing, waves surpassing wind in energy source, 
            methane pumped from lakes, lifelike voice synthesis by computers, 
            technology voice and gesture activated, houses and fabrics smart, 
            and nano-technological weapons sought by terrorists.<br>
            Implication: For HR, this would imply embracing new technologies and 
            providing the best available to employees, working together with IT 
            to tackle interface problems, providing English language training 
            for voice recognition, and networking the organisation and the 
            people.<br>
            In terms of economics, physical money will have disappeared, 
            transactions will be virtual, digital and universal, corporations 
            will (re-)open their own education institutions, and the paradigm of 
            the workplace will be the network.<br>
            Implication: For HR, this would imply a need to benchmark against 
            global rates of pay, and a knowledge that outsourcing in developing 
            economies offers a 3-5 year window of lower salaries, after which it 
            rises. There will be pressure on global companies to provide the 
            benefits of the home country to workers in offshore countries. <br>
            In terms of health, longevity will continue to increase, as well as 
            obesity in developing nations. <br>
            And healthcare and drug prescriptions will be individually tailored. 
            There will be an increase in the number of people living alone, with 
            related problems of drug abuse and violence. Biosensors will be 
            worn, cancer will be replaced in the West as biggest killer, and 
            global epidemics will increase. <br>
            Implication: HR will need to provide wellness programmes and 
            psychological healthcare, adapt the workplace for older workers and 
            larger workers, and adapt HRIS to include more information on health 
            of workers.<br>
            In 2020, in terms of government, globalisation will challenge our 
            concept of what is right, culture clashes will destabilise countries 
            with large immigrant populations, conflicts will be faster and 
            safer, the power of networks of organised protestors will grow, and 
            cultural, ethnic and religious minority groups will no longer 
            sublimate their identities to nation-states.<br>
            Implication: For HR, this will mean monitoring and using the power 
            of networks, tracking allegiances across boarders, providing 
            multi-cultural/ intercultural workplaces – with supporting 
            education, and participating in the creation of new legislation.<br>
            Society will see home and work places blended – not balanced, an 
            increase in the number of people living alone – requiring amusement 
            and housing, an affluent move from cities to beauty spots, small 
            elite garnering enormous power and wealth – but without noblesse 
            oblige of former class systems, elites operating in an international 
            arena – bypassing nation states, telecities replacing megacities, 
            and possibly the death of the achievement culture. <br>
            Implication: HR will need to provide spiritual and social succour, 
            support programmes for individuals in how they work – not where they 
            work. It will need to plan for the amoebic organisation, make 
            working more interesting than not working, and network people 
            together.<br>
            In terms of demographics, the world population growth will 
            decelerate, the youth boom in Asia will age – bringing healthcare 
            and social security challenges, there will be more labour shortages 
            (10 million worker shortfall in the US by 2010), and the retiring 
            age will rise across the world. Only 1 in 5 workers in UK by end of 
            this decade will be white, male and full-time.<br>
            Implication: HR will need to appeal to workers who are older, 
            ‘foreign’, from minorities, and carers. It will need to use robots, 
            and maintain the fight for talent.<br>
            <br>
            The conclusion<br>
            In 2020, there will be a dearth of leaders. HR will need to attract 
            experienced leaders from retirement by offering exciting challenges, 
            and allow them to maintain their lifestyle, live where they want, 
            and work part-time. It will need to grow the next generation itself, 
            and meet their different expectations. It will need to compete with 
            dot.coms and starter businesses. <br>
            HR will need to adopt ethical trading – 25 per cent of employees 
            believe that employers’ ethical positions are more important than 
            their pay (Work Foundation), and consider the <br>
            expectations of employees, as well as shareholders. HR will have to 
            do more than the legal minimum, and encourage whistle blowers and 
            mystery shoppers.<br>
            HR will need to shape the organisation – by deciding what kind of 
            employer it wants to be, positioning itself to attract people who 
            will stay. Will there be a heterogeneous or monogeneous workforce? 
            HR will need to agree contractual engagements that are individual 
            and various.<br>
            It will need to act as carer and coach – by increasing the use of 
            assessment and development centres, using psychologists/coaches to 
            help people work in new ways, understanding and accepting individual 
            difference, allowing people to be themselves rather than trying to 
            change them, and providing spiritual and emotional care. <br>
            Karen summarised that HR would need a plethora of contracts, lay on 
            a menu for people, work locally and globally, and be a family and a 
            business. HR would need to be flexible – and drawn on the humanity 
            in all of us.</font></p>
