<p align="justify"><font face="Verdana" size="4" color="#FF0000">Islamic Finance<br>
</font><font face="Verdana" size="1">
<br>
<b>Islamic finance has emerged as one of the most important trends in the 
financial world. Ali Hassan Moosa takes a closer look</b></font></p>

<p align="justify"><font face="Verdana" size="4" color="#FF0000"><img border="0" src="im80.jpg" align="left" hspace="5" width="125" height="147"></font><font face="Verdana" size="1">I</font><font face="Verdana" size="1">slamic 
finance is comparatively a small sector of international finance, but 
nonetheless, one that is growing at a rapid pace, around 15 per cent per annum. 
Today, economists estimate Islamic finance’s net worth to be in the range of 
$200 billion to $250 billion (RO77 billion to RO96 billion), more than 40-fold 
in less than 20 years since it came into existence through modern financial 
intermediaries. <br>
The basic principle of Islamic finance is the prohibition of ‘Riba’ (usury or 
interest). This principle is derived from ‘Sharia’; laws governing society in 
Islam. This principle is not unique to Islam as usury was also prohibited in 
both the Old and New testaments of the Bible. <br>
Like conventional banks, Islamic banks are financial intermediaries and trustees 
of other people’s money with the difference that they share their profits and 
losses with their customers. This difference introduces an element of 
partnership in Islamic banking by providing some virtual ownership rights to the 
customers.<br>
Islamic finance relies solely on the principle of profit/loss-sharing between 
stakeholders. Such a rule is also applicable to bank depositors who may gain 
variable return on their deposits depending on the profitability of the bank. 
This by no means indicates that Islamic financial institutions are relatively 
higher risk and enter into speculative activities.<br>
Arguably, the profit-sharing principle should, in theory, make profitability 
less volatile over a period of time. Islamic banks also benefit from large 
concentration of cheap (low cost) deposits, considerably reducing their cost of 
funding.<br>
In terms of risk management, Islamic financial institutions may be at a 
disadvantage compared to conventional banks. The default risk factor is higher 
for Islamic banks since default penalties and accrued interest may act as a 
deterrent to default in conventional banking. Such practices are, of course, not 
permissible under Sharia laws. <br>
On the other hand, because of the involvement in the customer’s business, 
Islamic banks would know what risks they are exposed to and would accordingly be 
able to diversify their activities as means of managing risk. A large number of 
bankers and financiers alike believe that there is a strong and growing demand 
for Sharia compliant products by Muslims across the globe. This has been evident 
not only in predominantly Muslim countries such as Malaysia and some GCC 
countries, but also in countries such as China, the US and the UK.<br>
There are a number of financing methods available in Islamic finance, all of 
which have been certified by prominent Islamic scholars and official religious 
bodies across the world. These methods include:<br>
<br>
<b>Murabaha</b><br>
Can be defined as cost-plus-sales or financing re-sale of goods. The financial 
intermediary purchases goods on behalf of the customer and gets in return a 
margin (lump sum or a percentage) from the sale proceeds of these goods. This 
method is seldom used in reality since there is no credit as the finance agency 
only acts as a middleman or broker.<br>
<br>
<b>Ba’y bi-thaman a’jil</b><br>
Can be defined as credit sales. Similar to Murabaha, except that the 
institution’s profit is deferred and received over a predetermined period of 
time. From a credit risk perspective, deferment would inherently mean higher 
risk and, therefore, a higher profit margin may apply depending, of course, on 
the risk assessment of the transaction.<br>
<br>
<b>Ijarah</b><br>
Ijarah is a substitute to conventional leasing. The only difference between the 
conventional lease and Ijarah is that the financial institution must own the 
asset throughout the duration of the lease. Ijarah is widely used as means of 
mortgage financing. Similar to conventional financing, finance lease involves 
the transfer of ownership to the lessee (customer) at the end of the duration 
whereas in the operating lease, the instalment is calculated based on the rental 
of the asset, wear-and-tear and profit. Lease payments (rentals) can be fixed, 
clearly exposing the institution to fluctuations in cost of funds, or can be 
floating by fixing the rentals over short-term durations.<br>
<br>
<b>Musharakah and Mudaraba</b><br>
Can be defined as partnership or participation financing. In Musharakah, both 
the customer and the financing agency own a share of the asset. Diminishing 
partnership, Musharakah Mustanqisah, is similar to a lease/Ijarah, except that 
the customer’s share over time increases in the asset because of the 
instalments, and the institution’s share diminishes over time until the end of 
the contract.<br>
<br>
<b>Salam and Istisna</b><br>
Can be defined as Islamic forward deal such as financing projects that will take 
shape in future. An example of Salam is payment now for future delivery of 
agricultural goods, and an example of Istisna is prepaid orders for 
manufacturers. There are, however, strict rules governing such transactions in 
order to minimise the risk of prohibited speculative activity.<br>
Similar to financing techniques described above, deposit accounts also operate 
on the principle of profit sharing. The profit element can be agreed in advance 
or can vary throughout the term of the deposit but based on periodic profit 
distribution. The investor’s capital (deposit) is normally guaranteed for 
current accounts, which are on demand, but may not be guaranteed for saving and 
investment accounts.<br>
Another popular financial product that has a growing market is the Sharia-compliant 
insurance product Takaful. It can simply be defined as cooperative insurance 
where a number of subscribers contribute to a pool of money that is invested 
wisely in Sharia-complaint instruments, and monies are withdrawn when a claim 
arises. The nature of such claims has stringent rules compared to conventional 
insurance.<br>
A number of financial intermediaries have unfortunately resorted to ‘window 
dressing’ solutions, i.e., offering products that seem to be Sharia-compliant 
but may not necessarily have a Sharia-compliant funding mechanism. It is really 
the duty of regulators and consumer protection watchdogs to ensure that the 
public is fully aware of the financing mechanism.<br>
Noting some of the recent developments in Islamic finance; one of the latest 
additions to the market is the Islamic Bank of Britain (IBB), which is planning 
to expand in a number of European Union (EU) countries in the near future. 
Recently, the global banking giant HSBC has launched a wide range of Sharia-compliant 
products branded as Al Amanah.<br>
Across the boarder in the UAE, the world’s first Islamic bank, born in 1975, the 
Dubai Islamic Bank has set up a financing subsidiary called ‘Tamweel’. The 
company provides Sharia-complaint mortgages and has achieved an impressive 
AED500 million ($136 million; RO52 million) worth of business in the first four 
months of operation. ‘Amlak’ is another finance company based in Dubai that 
specialises in mortgage lending. Due to the strong demand on Sharia-compliant 
products, it had recently converted its operations from conventional financing 
to Islamic financing. <br>
Similarly, the National Bank of Sharjah was the first bank in the world to 
convert from conventional banking to Islamic banking. Abu Dhabi Islamic Bank is 
a relatively new entrant in Islamic banking, but has uniquely positioned itself 
by offering innovative and contemporary Sharia-compliant products.<br>
The local economy is unique in the region as no financial intermediary offers 
Sharia-compliant financial products and services. However, lately, one of the 
locally set up finance companies was granted a licence by regulators to operate 
‘unconventional’ financing, allowing them to offer lease-based arrangements 
similar to Ijarah. <br>
It is widely believed by analysts that the absence of Islamic banks in the 
Sultanate is one of the factors behind the country’s small deposit base, which 
is around 35 per cent of Gross Domestic Products . Officially published figures 
suggest that a large number of deposits do end up in banks abroad, arguably 
Islamic banks.<br>
Worldwide, the standardisation of financial accounting and regulation of Islamic 
financial intermediaries is still an unresolved issue. It would, therefore, be 
wise for the regulators in the region, most importantly within the GCC, to agree 
to a common framework under which this eminent segment of the market ensures 
transparency and proper accountability in order to significantly participate in 
the development of national economies.<br>
