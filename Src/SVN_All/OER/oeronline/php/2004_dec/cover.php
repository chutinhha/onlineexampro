            <b>Halliburton accused of overcharging for gas </b><br>
            Two former employees claimed that the oil services company, which 
            secured no-bid contracts for reconstruction projects in Iraq, 
            “routinely overcharged” the US military. Halliburton, formerly 
            headed by US vice-president Dick Cheney from 1995 to 2000, was under 
            investigation by several government agencies. <br>
            The company drew criticism from Senator John Kerry and other 
            Democrats during the election campaign later in the year because of 
            the no-bid contracts it received for much of the Iraqi work, 
            especially after military auditors found evidence of possible 
            overcharging for some work.<br>
            Halliburton executives said previously they expected criticism to 
            die down after the election and that the company's share price, 
            which trades at a discount to its peers such as Schlumberger and 
            Baker Hughes Inc., would rebound. But the company still faces a 
            number of problems over its Iraq contracts. <br>
            In the latest move, the US Army Corps of Engineers’ top contracting 
            official called for an investigation into the award the of 
            multi-billion dollar contracts to Halliburton, calling it the worst 
            case of contracting abuse she had seen. <br>
            Bunnatine Greenhouse, in a television interview, said, ‘‘It was 
            misconduct, and part of the misconduct was blatant.’’ At the centre 
            of Greenhouse’s complaint is a no-bid contract worth $7 billion 
            (RO2.7 billion) awarded last year to KBR to rebuild Iraq’s oil 
            industry. US auditors have also accused KBR of being unable to 
            account for over a third of the items it handled in Kuwait under a 
            work order for the US occupation authority in Iraq. <br>
            In addition, the US Army is threatening to withhold payment of 15 
            per cent, or about $600 million (RO230.77 million), of Halliburton’s 
            bills because of a dispute over whether KBR properly documented its 
            bills for feeding and housing troops in Iraq and Kuwait. Halliburton 
            has consistently denied any wrongdoing in its work in Iraq, where 
            KBR’s contracts have brought in $5.2 billion (RO2 billion) in 
            revenues so far this year (till November) and about $8.8 billion 
            (RO3.38 billion) since the start of the war last year. <br>
            <br>
            <b>GCC citizens can own property in Oman</b><br>
            A Royal Decree was issued allowing citizens of the Gulf Cooperation 
            Council (GCC) to own property in Oman. “GCC citizens would be 
            allowed to rent and own built real estate or land for the purpose of 
            residence or investment in any member state and would be treated as 
            citizens of the state where the real estate was situated,” said the 
            decree. But land acquired by a GCC citizen in Oman would have to be 
            put to use within four years or the State would have the right to 
            reclaim it back after “giving its owner appropriate compensation,” 
            according to the Decree.<br>
            <br>
            <b>Scientists clone human embryos</b><br>
            Scientists in South Korea announced that they had created 30 human 
            embryos by cloning. The scientists had taken the genetic material 
            from normal cells in women donors and combined it with their eggs. 
            The resulting embryos were then grown up to produce so-called stem 
            cells that could divide into any tissue in the body. The aim was to 
            use the cells to replace ones that had failed in patients with 
            problems such as Alzheimer’s disease. <br>
            <br>
            <b>OLNG celebrates 10th anniversary</b><br>
            <img border="0" src="im9.jpg" align="right" width="150" height="112">Oman LNG completed 10 years in the business. His Majesty Sultan 
            Qaboos Bin Said had issued a Royal Decree on February 9, 1994, 
            establishing Oman LNG as one of the Sultanate’s biggest new 
            industries. Oman LNG is one of the leading companies in the global 
            LNG industry. It was reported that investments in the country’s 
            manufacturing sector had witnessed an annual growth of 44 per cent 
            during 1997-2001, significantly because of the commencement of LNG 
            production. Oman LNG, who would be setting up its third train of LNG 
            at Qalhat, finalised a long-term sales contract for 1.6 million 
            tonnes annually with Union Fenosa of Spain.<br>
            <br>
            <b>Pakistani scientist admits to selling weapons technology</b><br>
            Pakistan's chief nuclear scientist, Abdul Qadeer Khan, confessed to 
            sharing nuclear technology with Iran, Libya and North Korea in a 
            12-page document presented to Pakistan president Pervez Musharraf, 
            who later pardons him on popular demand. <br>
            <br>
            <b>Researchers report new elements</b><br>
            Scientists created two new chemical elements, named Ununtrium 
            (Element 113) and Ununpentium (Element 115). Each had many more 
            protons and neutrons in their nuclei than any naturally occurring 
            elements found on Earth. The discovery would encourage researchers 
            hoping to fill apparent gaps in the periodic table with ‘islands’ of 
            stable, massive atoms that may exhibit new chemical properties and 
            atomic shapes.<br>
            <br>
            <b>Bird Flu impacts business</b><br>
            <img border="0" src="im10.jpg" align="left" hspace="5" width="150" height="111">Bird Flu (Avian Influenza) raced through poultry farms in a almost a 
            dozen Asian countries from Japan to Pakistan, where tens of millions 
            of birds were slaughtered in an effort to check its spread. Demand 
            for boneless meat, estimated at 4,000 tonnes annually, was met 
            almost exclusively from Brazil at higher prices.<br>
            <br>
            <b>Oman Mobile comes into being</b><br>
            In keeping with the government’s policy of deregulating the 
            country’s telecommunications sector, Omantel announced the 
            incorporation of a separate mobile company called Oman Mobile 
            Telecommunications Company LLC. The new company became the first 
            firm licensed to provide mobile telecommunication services in the 
            Sultanate. <br>
            The establishment of the new mobile telecommunications company 
            followed the issuing of two licenses for Omantel, the first for 
            providing basic fixed general telecom services for 25 years and the 
            second for providing basic mobile telecom services for 15 years, 
            under a Royal Decree. As a part of its drive to extend its services, 
            Omantel later introduced broadband to its customers.<br>
            <br>
            <b>PDO begins oil production at Zalzala</b><br>
            <img border="0" src="im11.jpg" align="right" hspace="5" width="150" height="147">Petroleum Development Oman (PDO) began to produce oil from the 
            Zalzala field, which is in the Harweel cluster of fields about 80 
            kilometres southwest of Marmul. The field has been producing oil and 
            gas from a depth of some 5 km, making it PDO’s deepest producing oil 
            field.<br>
            <br>
            <b>Blair meets Gaddafi</b><br>
            In a State visit that marked Britain’s willingness to welcome Libya 
            back into the international community, British PM Tony Blair met 
            with Libyan leader Moammar Gaddafi, saying that the two shared a 
            common goal in defeating the al-Qaeda terrorist network. The British 
            PM and the Libyan leader promised to battle terrorism. <br>
            The visit ended nearly 30 years of diplomatic isolation of Libya, 
            which announced that it would surrender its nuclear weapons. Blair 
            promised not to forget “the pain of past actions,” such as the 1988 
            Lockerbie bombing by Libyan agents, which killed 270 people. But he 
            added that Libya should be welcomed back into the international 
            fold. <br>
            Britain has taken the diplomatic lead in ending Libya’s 
            international isolation, and Blair’s visit—the first by a British 
            leader since Gaddafi seized power in 1969—was seen as Gaddafi’s 
            reward for dismantling his nuclear weapons program and renouncing 
            terrorism.<br>
            <br>
            <b>EU Commission rules against Microsoft</b><br>
            The European Commission (EC) fined the software giant Microsoft 
            RO232 million ($603 million) and ordered the company to stop 
            bundling its products, such as Windows Media Player, with its 
            operating system. The EC ordered the company to sell a version of 
            Windows in Europe with Windows Media Player software code stripped 
            out. <br>
            This ruling raised the threat that the EC would regulate how 
            Microsoft integrates new features into Windows. Microsoft had also 
            been ordered to provide valuable proprietary software code and 
            technical information to competing companies. Steve Ballmer, chief 
            executive officer of Microsoft, had said that in spite of the 
            ruling, the company “would continue to invest in new technology 
            breakthroughs, and would continue to work to bring innovations.” <br>
            <br>
            <b>Oman International Rally</b><br>
            <img border="0" src="im12.jpg" align="left" hspace="4" vspace="2" width="150" height="122">Top rally drivers from the Middle East participated in the Oman 
            International Rally, the third round of the 2004 FIA Middle East 
            Rally Championship. The event was held under the patronage of the 
            Royal Oman Police and Mohsin Al Balushi, Under Secretary of Tourism. 
            The rally took place as a championship round for the first time 
            since 1998, although the Oman Automobile Association ran a candidate 
            event last October. Action started with a timed two-km Super Special 
            stage in the wadi behind the Al Araimi Complex in Qurum. Nasser Al-Attyia 
            from Qatar and Creis Patterson from Great Britain won the rally on 
            their Subaru Impreza.<br>
            <br>
            <b>Highway ambulance service launched</b><br>
            The Highway Emergency Ambulance Service (HEAS) was launched at the 
            Police Stadium, Wattayah. The service, the first of its kind, would 
            provide quality and urgent pre-hospital medical care for road 
            accident victims at the scene of a traffic accident. The Ambulance 
            Unit was to operate from six round-the-clock service centres in 
            Muscat Governorate initially, and was scheduled to expand to cover 
            other parts of the Sultanate. Each team from the centre comprised of 
            three first-aid specialists. The number was to be increased 
            gradually.<br>
            <br>
            <b>Sultan Qaboos Chair at Harvard U</b><br>
            A chair was established at the Harvard University in the US in the 
            name of His Majesty Sultan Qaboos bin Said. The ‘Sultan Qaboos 
            Professorship Chair in International Relations’ would conduct 
            research and teach contemporary international relations with special 
            focus on the role of regional and global cooperation. A ceremony was 
            held at Harvard University in the US to inaugurate Chair. HE Yusuf 
            bin Alawi bin Abdallah, the minister responsible for foreign 
            affairs, and HE Mohammed bin Ali bin Thani al Khusaibi, Oman’s 
            ambassador to the US, attended the ceremony.<br>
            <br>
            <b>US ends trade ban with Libya</b><br>
            Citing Libya’s decision to end pursuit of weapons of mass 
            destruction, US president George Bush lifted trade restrictions 
            against the country. The US relaxed its trade embargo on Libya to 
            allow US firms to buy Libya’s oil and invest in its economy for the 
            first time since 1986 in a reward for Tripoli giving up weapons of 
            mass destruction. <br>
            White House spokesman Scott McClellan made it clear that the US 
            actions stemmed from Libya’s surprise decision on December 19, 2003, 
            to give up its pursuit of chemical, biological and nuclear arms, and 
            its work since to eliminate its stockpiles and programs. ‘‘Libya has 
            set a standard that we hope other nations will emulate in rejecting 
            weapons of mass destruction,’’ he had said.<br>
            <br>
            <b>Abuse reported at Iraqi Prison</b><br>
            CBS’ 60 Minutes II broadcast graphic photos, taken in late 2003, of 
            American soldiers grinning as they abused Iraqis in the Abu Ghraib 
            prison. Images sparked outrage around the world, especially in the 
            Middle East. British PM Tony Blair condemned the pictures but 
            defended the thousands of other British troops serving in Iraq. A 
            representative of the Mirror newspaper, which carried photographs of 
            abuse on its front page, told CNN in London that the photographs 
            were obtained from British soldiers who had returned from serving in 
            the region. Blair had said: ‘‘Let me make it quite clear that if 
            these things have actually been done, they are completely and 
            totally unacceptable. We went to Iraq to get rid of that sort of 
            thing, not to do it.”<br>
            <br>
            <b>Omantel launches MaxNet</b><br>
            <img border="0" src="im13.jpg" align="right" width="150" height="102">The government’s drive to usher in the digital age in the Sultanate 
            received a major boost with the launch by Oman Telecommunications 
            Company of its ATM Corporate Broadband Solution. The new service, 
            introduced under the brand name MaxNet, was based on a nationwide 
            multi-service broadband platform, which allowed the convergence of 
            data, voice and video services.<br>
