                    Cover Story<br>
            <br>
                    </font><font face="Verdana" size="4" color="#FF0000">Events 
            that shaped the year</font></p>
            <p align="justify"><b><font face="Verdana" size="1">
            OER chronicles major local, regional and international events that 
            shaped 2004, a year that will probably go down in history as the 
            year when expensive oil became reality. </font></b><font face="Verdana" size="1">
            <b>We look back at the fading backdrop of the year that was, and 
            attempt to catalogue, analyse, investigate, scrutinise, dissect, 
            evaluate and probe the key events that had or are expected to have a 
            major impact on the region’s business and economy in general, and 
            that of Oman in particular<br>
            </b><br>
            <img border="0" src="cover1.jpg" align="left" width="215" height="225">There 
            are no uneventful years. Only years that whiz past you faster than 
            you can say “Turmoil!” As 2004 takes its final step towards being 
            etched into history, the one thing that the year will surely be 
            remembered for is the oil price gone crazy. From an average of $31 
            per barrel in 2003, the price of the benchmark West Texas 
            Intermediary (WTI) peaked around $51 per barrel in early September, 
            and was hovering around the $49-mark at the time OER went to Press 
            in late November. Despite the war in Iraq getting over last year, 
            the continuing geopolitical instability in the region ensured that 
            the ‘fear premium’ remained high. <br>
            The reasons for such a high price, according to analysts, vary from 
            the steady increase in the Asian economies’ thirst for oil to the 
            mixed signals sent by the Russian government with regard to Yukos’ 
            future and the speculative activity on the part of market movers. 
            The truth may be a combination of all of these, and much more. <br>
            Another reason that is being cited now more regularly than ever 
            before for the spiralling oil price is the systematic and utter 
            dominance of the once brawny dollar by currencies such as the pound, 
            the yen and the euro. From a little less than a dollar to the euro 
            in January 2003, it takes $1.32 to buy a euro today (November 26, 
            2004), an increase of over 33 per cent in a matter of less than two 
            years. The London P.M. gold price, or the afternoon fix as it is 
            more popularly known as, ended 2003 at $417.25 which, incidentally, 
            was that year’s highest quoted price, and is today hovering around 
            $451 (November 26, 2004). <br>
            <img border="0" src="im1.jpg" align="right" hspace="5" width="120" height="201">The passing year saw the re-election of US President George W. Bush 
            Jr for a second term in office, defeating John Kerry by a margin of 
            3 per cent of popular votes. This happened in the same year that saw 
            the unearthing of the Abu Gharib prison abuse scandal, in which 
            haunting images of Iraqi prisoners’ abuse by some of the US and 
            British troops were splashed across the media in US and elsewhere.
            <br>
            The year witnessed the death of Palestinian leader Yasser Arafat and 
            UAE President Sheikh Zayed bin Sultan Al Nahyan in the month of 
            November, and that of former US president Ronald Reagan’s in June. 
            In the worst natural disaster of the year, an Earthquake, measuring 
            6.4 on the Richter scale, killed as many as 628 people near north 
            coast of Morocco on February 24, 2003. While OER has no record of 
            the number of lives lost in ‘man-made’ disasters, some estimates put 
            the death toll in just Iraq this year as high as over 10,000. <br>
            The year 2004 wasn’t all about bad news, though. The Rover landed on 
            Mars amid hopes that there might have been life on the planet. Two 
            new chemical elements—Ununtrium and Ununpentium—were created while 
            scientists in South Korea announced to have cloned human embryos. 
            Ten new countries joined the 
            <img border="0" src="im2.jpg" align="left" hspace="4" vspace="2" width="200" height="149">European Union, bringing the number of 
            member states grew to 25. The 63-year-old Michael Melvil became the 
            first civilian pilot to steer a spaceship. <br>
            Closer home, UAE-based group won two mobile phone licences in Saudi 
            Arabia while Oman itself witnessed huge advances in its 
            privatisation drive, with the second mobile licence being awarded to 
            Nawras Telecom, a consortia led by Qatar’s Q-Tel, in June. In 
            anticipation, the government formed a new mobile company, Oman 
            Mobile, in March, and slashed tariff rates by up to 30 per cent. <br>
            Oman’s media law was also amended, with the new law allowing 
            privately owned radio and TV stations to be set up in the country. 
            The Ministry of Manpower signed training agreements worth RO1.7 
            million with companies in the private sector in August. According to 
            ministry statistics, 54,885 male and female job seekers were 
            enrolled at the ministry from January 1, 2004 to October 31, 2004. 
            And, 9,020 male and female citizens were selected for vacant posts 
            in the private sector while 31,202 job opportunities were availed in 
            the public sector. <br>
            <img border="0" src="im3.jpg" align="right" hspace="4" vspace="2" width="200" height="164">True to its promise of giving the tourism sector a boost, the 
            country saw the establishment of the Ministry of Tourism in June, 
            with HE Rajha bint Abdulameer, formerly undersecretary at the 
            Ministry of National Economy, being appointed as the Minister of 
            Tourism. The Sultanate also saw the launch of a host of development 
            initiatives, including the unveiling of $805-million The Wave 
            seafront resort tourism project. The government relaxed the 
            country’s property laws, allowing GCC nationals to own property in 
            the Sultanate. The country saw impressive advances in its stock 
            market, the Muscat Securities Market (MSM), with the index posting 
            over 25 per cent improvement over its 2003-end figures.<br>
            Hindsight is always 20:20. Whether disasters could have been averted 
            or better progress made in the time now past has been and will 
            always remain open for debate. But that is what life is all about – 
            a potent mix of struggles won and lost, opportunities missed and 
            gained, and feats accomplished and failed. <br>
            While we acknowledge that it is, of course, impossible to capture 
            all the events and happenings of the year in a few pages, this is 
            our attempt at chronicling the major events that we think may have 
            an impact on the time to come. <br>
            <br>
            <b>Budget 2004 unveiled</b><br>
            <img border="0" src="im4a.jpg" align="left" width="150" height="194">HE Ahmed bin Abdulnabi Macki, the minister for national economy and 
            vice-president of the Financial Affairs &amp; Energy Resources Council, 
            announced a growth-oriented deficit budget. Projected revenue and 
            expenditure were RO2,925 million and RO3,425 million, respectively, 
            leaving an anticipated deficit of RO500 million. The government was 
            planning to borrow RO240 million from domestic and overseas markets 
            to partially cover the budget deficit. However, this deficit of 
            RO500 million was based on a projected conservative average oil 
            price of $21 per barrel. With Oman’s crude oil price for the month 
            of May 2004 reaching $35.45 per barrel, the highest for 20 years, 
            the projected deficit should have turned into a surplus. However, 
            declining PDO production may have reduced the amount of final 
            surplus recorded in 2004.<br>
            <br>
            <b>Budget Highlights</b><br>
            • Negative growth of 9 per cent in GDP due to anticipated decline in 
            production.<br>
            • Oil production estimated to drop to 710,000 bpd against 763,000 
            bpd budgeted in 2003.<br>
            • RO500 million deficit budgeted based on oil price of $21 per 
            barrel.<br>
            • Investment expenditure projected at RO912 million - an increase of 
            48 per cent arising mainly from LNG and oil condensate investment 
            expenditure of RO255 million.<br>
            • Corporate tax revenue budgeted at RO55 million.<br>
            • Customs revenue estimated to rise by 10 per cent to RO55 million.<br>
            • The government was to divest 30% of Omantel’s equity.<br>
            <br>
            <b>
            <img border="0" src="im5.jpg" align="right" hspace="5" vspace="2" width="145" height="167">Al Kamil Power plant inaugurated</b><br>
            The 285MW Al Kamil Power Plant was opened by HH Sayyid Shabib bin 
            Taimour al Said, advisor for environmental affairs to HM Sultan 
            Qaboos bin Said. The RO48 million plant consists of three GE 9E gas 
            turbines and would replace diesel plants in the wilayats of Sur, Al 
            Kamil Wa’l Wafi, Bilad Bani Bu Hassan, Al Mudhaibi and Al Mudhairib.<br>
            <br>
            <b>Salalah gas pipeline inaugurated</b><br>
            HE Sheikh Mohammad bin Ali Al Qatabi, minister of state and governor 
            of Dhofar, inaugurated the RO68 million Salalah gas pipeline at 
            Raysut Industrial State. The 700-km pipeline is expected to produce 
            4 million cubic metres of gas a day.<br>
            <br>
            <b>Spirit rover lands on Mars</b><br>
            The Spirit rover survived the plunge through the Martian atmosphere 
            after a seven-month voyage from Earth. The second rover, 
            Opportunity, also lands on Mars. <br>
            <br>
            <b>
            <img border="0" src="im6.jpg" align="left" vspace="2" width="150" height="192">Fahud-Sohar gas pipeline project inaugurated</b><br>
            HE Ahmed bin Abdulnabi Macki, minister of national economy, 
            inaugurated the 300-km Fahud-Sohar pipeline project worth RO47 
            million on the OGC site at Sohar Industrial Port. The project 
            allowed the transport of 91.5 million cubic metres of gas per day.<br>
            <br>
            <b>Smart ID cards launched</b><br>
            Oman became the first Arab country to introduce a machine-read smart 
            card with stored thumbprint when the country’s first ID card, 
            belonging to His Majesty Sultan Qaboos Said, was issued in the first 
            month of the new year.<br>
            <br>
            <b>RO105 million power plant inaugurated</b><br>
            HE Sayyid Badr bin Saud bin Hareb Al Busiadi, minister of defence 
            affairs, inaugurated the 242MW and RO105 million Salalah power 
            project. The new power station replaced diesel-fired stations in 
            Salalah, Raysut, Mirbat, Sadah and Thumrait.<br>
            <br>
            <b>Virus affects millions of computers</b><br>
            Worm, called My Doom or Novarg, spread through Internet servers. 
            About 1 in 12 e-mail messages got infected and 1.2 million computers 
            got effected in just 24 hours! This was perhaps the worst virus till 
            date.<br>
            <br>
            <b>US begins fingerprinting foreign visitors</b><br>
            The United States began fingerprinting and photographing visitors 
            from most countries in a controversial programme to try to prevent 
            potential terrorists from slipping in by plane or ship.<br>
            <br>
            <b>
            <img border="0" src="im7.jpg" align="right" hspace="5" width="150" height="220">Report exonerates Blair</b><br>
            British judge Lord Hutton said Prime Minister Tony Blair did not 
            intentionally exaggerate intelligence on Iraq’s weapons.<br>
            <br>
            <b>Parmalat scandal unearthed</b><br>
            The much publicised unravelling of Italy’s eighth-largest industrial 
            empire, the food giant Parmalat, caused reverberations around the 
            world. The flagship Italian producer of some of the most popular 
            dairy products, collapsed, and its founder Calisto Tanzi was jailed. 
            Major players from the world of finance were also under scrutiny.<br>
            <br>
            <b>Former Enron CFO pleads guilty</b><br>
            Andrew Fastow admitted to covering up Enron’s financial woes and 
            defrauding the company. His wife, Lea, pleaded guilty to a tax 
            felony.<br>
            <br>
            <b>The Wave tourism project unveiled</b><br>
            Oman’s largest tourism development project till date—The Wave 
            seafront resort—was launched in February. Stretching along 7.3 km of 
            pristine beachfront just west of Muscat and occupying 195 hectares 
            (2mn sq m), the $805 million (RO 310mn) resort project is being 
            developed with the help of the Dubai-based Majid al Futtaim Group, 
            which committed RO35mn to develop Phase I of The Wave later in 
            September 2004. The project involves land reclamation, 
            infrastructure development and site improvement including a golf 
            course and a marina.<br>
            ‘‘The Wave is designed to contribute to the experience of Oman, a 
            country with a fascinating heritage and astonishing natural 
            beauty,’’ explained HE Mohsin bin Khamis Al-Balushi, Oman’s 
            undersecretary of tourism, at the launch party. “We have a clear 
            vision for The Wave which will be a fully integrated premium tourism 
            and residential resort in Oman. The country offers graceful living 
            in an authentic atmosphere unspoiled by commercial excesses, and is 
            not flooded by low-end package tours,” he had said at that time. The 
            Sultanate promised to contribute the original land and part of the 
            equity, and domestic and international investors were invited to 
            join forces with the government.</font></p>
