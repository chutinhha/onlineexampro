<p align="justify"><font face="Verdana" size="4" color="#FF0000">Solving the 
Oman Paradox</font><font face="Verdana" size="1"><br>
<br>
<b>Oman figures amongst the top countries in the economic freedom index, but low 
income levels remain a point of worry. In an attempt to find out the degree of 
economic freedom in Oman, and the factors influencing it, a roundtable 
discussion was held under the auspices of OER. An exclusive report</b><br>
<br>
<img border="0" src="im30.jpg" align="left" vspace="2" width="300" height="188">The 
key ingredients of economic freedom are personal choice, voluntary exchange, 
freedom to compete and production of person and property. However, economic 
freedom also requires governments to refrain from certain activities – actions 
that interfere with personal choice, voluntary exchange, and the freedom to 
enter and compete in labour and product markets.<br>
In an attempt to find out the degree of economic freedom in Oman, and the 
factors influencing it, a roundtable discussion was held under the auspices of 
Oman Economic Review.<br>
The panel consisted of the OER’s editor-in-chief and director of Renaissance 
Services, HH Sayyid Tarik Bin Shabib; Majid Al Toky, partner, Trowers &amp; Hamlins; 
Rajeev Singh, partner (Business Advisory Solutions), Ernst &amp; Young; Sheikh 
Mohammad Al Harthy, chairman, Oman Economic Association; Minoo Saher, group CEO, 
Mustafa Sultan Enterprises; and Dr Fahim Al-Mahrubi from the Economic and 
Finance Department of Sultan Qaboos University. Khalil Ahmed Al Harthy, 
Assistant General Manager, Al Ahlia Insurance moderated the discussion.<br>
A presentation by Michael Walker, Executive Director, Fraser Institute, set the 
ball rolling by introducing the ‘Oman Paradox’ to the panel – the relationship 
between economic freedom and economic performance. “Economic freedom is a 
powerful magnet for investment,” he said. When people are free to make their own 
choices, they invest in the future. So will foreign investors because they 
understand the opportunities economic freedom creates. That leads to greater 
prosperity and opportunity for everyone.<br>
‘‘What brings me to the Arab world and Oman in particular is to measure the 
economic freedom index – to better understand the economy and, hopefully, get 
people to take interest in the measurement of this freedom index. The idea is to 
provoke a discussion. My intent is not to offer solutions, as I am not an expert 
on Oman but an expert on the measurement of a variety of things related to 
economic freedom. I think that Oman presents a paradox even though it is very 
near the top of the economic freedom index, which is rated on a level of 10,” 
said Walker.<br>
Some of the questions that the presentation raised were as follows: <br>
1. What impact does the policy of Omanisation have, if any?<br>
2. Do job reservation laws curb the interest of potential foreign investors?<br>
3. Is there an interest in encouraging competitive pressure on Omani business?<br>
4. Is there an Omani insider-outsider problem in Oman?<br>
OER presents excerpts from the dialogue which took place over an extended 
period:<br>
<br>
<b>Michael Walker (MW):</b> Normally, a high performance on the economic freedom 
index means higher income levels, as is the case with some of the leading 
economies of the world like the US and Japan. One would, therefore, be correct 
to expect Oman, which ranks high on the economic freedom index (above seven), to 
have a higher income, but that is not the case. The level of income in Oman is 
low keeping in mind the high ranking in the index. So, we encounter our first 
paradox here: Oman is high up in the economic freedom scale but the level of 
income is low.<br>
One of the explanations that is often offered for Oman’s underperformance (in 
income levels) is that it has had a high birth rate. Population might play some 
role, but the reason is not very convincing as certain other countries have had 
more rapid population growths (UAE and Kuwait, for example) but are still 
growing and keeping their income levels higher than Oman. Although it was 
pointed out panel that the UAE and the Kuwait population growth was made up of 
expat professionals.<br>
Education level is another reason often cited for lesser incomes. Our findings 
about tertiary education in Oman reveal that the Sultanate fares poorly in this 
segment. One would again be tempted to regard the low level of tertiary 
education to be the culprit and that more effort in this front would solve 
Oman’s problem. But when we examine other countries, it presents an interesting 
picture. Mauritius has a tertiary education enrolment rate that is almost twice 
that of Oman; in Chile, it is five times as that of Oman; and Panama is almost 
four times more. All these countries have lower income levels than Oman!<br>
<br>
<b>HH Sayyid Tarik Bin Shabib (HHST): </b>We have a lot of people who are 
enrolled beyond the border. Do the figures represent tertiary enrolment within 
the country or do they also take count of enrolments abroad? <br>
MW: We need to verify that. But the point that I am making is that just getting 
more tertiary education enrolment is not going to solve the problem. It is not 
the magic bullet that some people think it is. We also found that UAE and Oman 
are quite below in terms of the female participation rate. If you forgo a very 
significant fraction of your total human capital, then that might be one of the 
factors for underperformance. I am talking here about the overall potential 
labour force in Oman. <br>
<b>HHST:</b> The indigenous workforce is quiet small in the Sultanate, and that 
would automatically mean lesser participation from the female work force. We 
have a lot of expatriates working in Oman…<br>
MW: This is not a comment on the people who are actually employed. We are 
talking about a female labour force that can be potentially employed. I also 
believe that there is an insider-outsider issue in Oman, and one of the stress 
areas for the government of the country is Omanisation…<br>
<b>HHST:</b> Omanisation is an imperative. We were in a comfortable position to 
employ people at modest wages. But now we have nationals looking for employment, 
people who will become increasingly competitive to get into the job market. It 
is really a moment of flux that we are in right now where there is pressure to 
employ Omanis in various sectors. It is a very complex equation. <br>
The most important question that crops up is that can you continue to feed in 
the people with the right skills in the opportunities as they arise? It is a 
very difficult task as it is at so many levels. Because you first have the very 
initial, basic training, then you have skill sets for a higher level that are 
incidentally not subsidised by the government. And then you have the actual 
subsidy by the government as a problem in itself because as long as it 
subsidises the training, why would anybody pay for it? <br>
From my point of view, one of the biggest challenges in Omanisation is that the 
policy is presented in a clear manner with the knowledge that what is going to 
happen in the next five years and you can plan your business around that. The 
current situation is that there is some trial-and-error going on as to what are 
the best ways of implementing this strategy. And trial-and-error by nature means 
that you cannot have a five or a 10-year vision on how employees are going to be 
inducted. And that I think is one of our biggest challenges. <br>
It is true that, today in Oman, companies are beginning to move into a situation 
where they can truly say that they are doing the work and are 100 per cent 
Omani-owned companies with desired Omanisation levels. But it is a very recent 
graduation.<br>
<b>MW: </b>One of the outstanding features of the Sultanate of Oman is the 
extent to which public officials seem to have other economic activities, which 
in some cases can put them in direct conflict with the public interest. In most 
of the other world economies, the actual taking on of a public function means 
not having a private interest so that there is no conflict. Interestingly, I 
believe that Oman scores very well on the world corruption index and the 
corruption level is very low. But I think people who are in government sector 
can make a clear distinction between the public interest and their own interest.
<br>
<b>Dr Fahim Al-Marhubi: </b>I think what we have learnt from the last 30 or 40 
years should be thrown out of the window. There is a conflict of interests. 
While discussing the economic freedom index for Oman, we cannot isolate a single 
problem like privatisation or infrastructure or conflict of interests. I believe 
that the paradox we are talking about is something to be expected given the gaps 
in governance, knowledge and educational attainments. <br>
The quality of our graduates needs to improve. That is why we have to rely on an 
expatriate labour force. We are a small country, but we have an unemployment 
problem. So, I do not think there is a single factor, but a multitude of factors 
including deficiencies in the institutional framework, issues of clashes of 
interests and indicators of quality. <br>
One of the startling takeout of the roundtable was that, in spite of Oman having 
an indifferent positioning on the indicators, instead of inflation, Oman 
actually had deflation! I believe inflation is one of the components that go 
into the creation of the index. And not many people believe that prices have 
actually gone down in the last 20 years. I, therefore, question the data that 
has been presented. The way I would put it despite the high level of economic 
freedom, which measures the overall institutional framework, somehow that is not 
being allowed to translate into the determinants of growth, which is capital 
formation and building up of human capital. The reasons are, of course, because 
of conflicts on interests the insider-outsider issue, etc.<br>
<b>Sheikh Al Harthy:</b> I think the economic freedom index covers a very wide 
area, and what we are discussing here are a few key aspects of the total 
picture. I think we need to dig deeper. The paradox exists because the Omani 
economy is getting good results, but these results are based on the government’s 
spending and the freedom that it provides. <br>
If we look at the developed nations, they have a distinction between the 
judiciary and the executive and freedom of the press. There is no conflict of 
interest there, and less obstacles for investment. All these factors have to be 
taken into consideration when coming to deal with the Oman paradox. <br>
<b>Minoo Saher:</b> I think this discussion has been an eye opener for anyone 
who wants to understand the dynamics of doing business in Oman. There are a lot 
of variables and sensitivities, which need to be kept in mind.<br>
<b>Khalil Ahmed Al Harthy:</b> I think that we all agree that the Oman paradox 
needs to be studied in detail to arrive at any kind of a conclusion. The Oman 
economy is looking up and this is the right time to iron out the rough edges.<br>
<b>Conclusion</b><br>
One has to face facts. With population growing, reduced oil production, and a 
globalising world, Oman can no longer afford to crowd out private sector 
entrepreneurs. More Omanis will join the economy every year, and the State 
cannot be expected to be the sole provider of new jobs. His Majesty’s government 
has the difficult role of fostering a sense of personal responsibility, 
inspiring and unleashing the drive of a generation, as it attempts to extricate 
itself from an economy in need of sustainable growth. The country has a superb 
record of political and monetary stability, and a dependable legal system. <br>
The next great leap will be for the government to have faith in the spirit of 
Omani enterprise and let the people decide the goods and services to be 
produced. Pressing ahead with such reforms could turn Oman into the Hong Kong of 
the Middle East. Gifted with a liberal economy, it has access through personal 
contacts to the growing markets of India, a government that has expressed the 
desire to make the necessary changes, and an openness to trade that makes World 
Bank economists grin with approval. </font></p>

