<p align="justify"><font size="4" color="#FF0000" face="Verdana">Understanding 
the WIIFMs</font><font face="Verdana" size="1"><br>
<br>
<b>James Graham, MCE change management faculty member, explains how a change 
initiative is affected by the ‘What’s In It For Me’ (WIIFM) dynamic<br>
</b><br>
<img border="0" src="im35.jpg" align="left" hspace="5" width="149" height="196">There 
is no doubt that the world is changing at an increasingly fast pace, as 
experienced by those who work in the financial services sector worldwide, and 
this brings a number of challenges that must be addressed. One can argue which 
country established global trade, the Phoenicians or another race, but what is 
certain is that international trading was well established by the birth date of 
Christ, or the equivalent reference in the diaries of other cultures and 
religions.<br>
During nearly two millennia (1 AD to late 1903), travel options were restricted 
to land or sea journeys. The Wright brothers’ flimsy aeroplane was of no 
practical use to business travellers; yet, only 60 years later, travel by jet 
aircraft was a commercial reality and the modern financial services professional 
uses airliners in the same way that the businessman of earlier would use trains 
and ships.<br>
It is not only travel that has experienced this rapid period of development, but 
business too; In fact, the pace of change is accelerating according to experts, 
an assertion that many people feel is reflected in their daily experiences. <br>
For instance, financial services workers in the 1960s would use many manual 
systems, developed and proven to work over hundreds of years, in an environment 
that was based around a ‘job for life’ value system. In other words, so long as 
you were hard working and conscientious, your future was assured by long-term 
tenure in your organisation.<br>
Forty years later, the young graduate joining a financial services organisation 
would find this model incredible. He or she (in itself a major change of the 
last 50 years) will typically expect to change employer three or four times in a 
career, with one or more of these changes created by external events. Whereas 
the 1960s’ employee would trust the organisation to provide a reliable, if 
rigid, career path based on mutual trust, the modern graduate will be likely to 
ask their employer “What’s In It For Me?” (Often abbreviated to the acronym 
WIIFM), meaning that the manager of the 2000s is often obliged to adopt a very 
different style to his or her predecessor of 40 years. <br>
The hierarchical, command and control model of management is no longer totally 
reliable, since it demands unquestioning loyalty and obedience from subordinates 
and the WIIFM concept conflicts deeply with this value system. <br>
<br>
<b>So how is a change initiative affected by the WIIFM dynamic?</b><br>
The good news for the contemporary manager is that the WIIFM concept can be a 
strong change driver and a powerful ally. It is a truism that people tend to 
resist change, unless there is a clear advantage for them and the WIIFM profile 
of individuals or groups helps the change manager to understand how to plan 
change to impact positively on those who will be affected by it.<br>
We call these people ‘constituencies,’ because this metaphor is helpful in 
remembering the dynamics of change. A constituency is defined in the Oxford 
dictionary as ‘‘a body of people who elect a representative,’’ and we can tune 
this to the world of change by amending it to read ‘‘a body of people who decide 
whether to support or resist a change.’’<br>
Observation suggests that there are three main attitudes to change amongst 
constituents: (1) Resist, (2) Avoid or (3) Support. Although this generalisation 
is a simplification (and there are several sub-categories under each heading), 
this view is sufficient for the purposes of illustration in a short article such 
as this and we shall briefly examine each category in the following paragraphs.<br>
If the constituency decides that the WIIFMs arising from a change initiative is 
weak, negative or non-existent, the result is usually resistance or, at best, 
apathy. The change manager must address this sort of attitude by understanding 
the WIIFMs and also putting things into perspective. It may be that the 
constituency is comparing the future against the present and this is often very 
unhelpful. <br>
To give a concrete example of how this can be done, the constituents of a 
Japanese company resisted a proposal to reduce salary levels, until it was 
explained that this was necessary to avoid compulsory redundancy. The loss of 
face to the individuals who would lose their job would have been extreme and it 
was preferable for all employees to ‘‘share the pain’’ by accepting the lower 
salaries; so, the change was accepted without further argument.<br>
Avoiding change is surprisingly easy in may large organisations; I remember 
interviewing a middle-level manager in a multinational company, who said quite 
openly ‘‘I have survived over 10 major change initiatives in my career, by never 
challenging or opposing and never responding to the message either.’’ As 
astonishing as this may seem, it is a common experience. <br>
The WIIFM for these people is often based on the principle that the effort of 
changing will be less than the trauma of not changing and this requires firm 
change management, especially in the area of monitoring/enforcing behaviours to 
check that people are following the new processes or procedures that change 
brings.<br>
Finally there are the supporters who embrace change as either leaders or 
followers. These people are more valuable than gold dust. The WIIFM is 
self-created and, ultimately, this is the most powerful WIIFM of all.<br>
So, it can be seen that ‘‘Understanding the WIIFMs’’ is not just a slogan, it is 
a practical way to support change initiatives, which can be applied by those 
with appropriate levels of experience in the field.<br>
