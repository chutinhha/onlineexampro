                  <p align="justify" style="margin: 3px 10px"> 
					<b><font face="Verdana" size="1" color="#C00000">&nbsp;</font><font face="Verdana" size="4" color="#C00000"><br>
					&nbsp;</font></b></p>
					<p align="justify" style="margin: 3px 10px"><b>
					<font face="Verdana" size="4" color="#C00000">Generating 
					confidence<br>
					</font><font face="Verdana" size="2"><br>
					Wadi Al Jizzi Power Company has achieved important 
					milestones and its credibility rating has gone up since the 
					time it began to generate power independently. A report by 
					Visvas Paul D Karra<br>
					<br>
					</font><font face="Verdana" size="4" color="#C00000">
				<img border="0" src="images/oct_power1.jpg" hspace="9" vspace="5" style="border: 0px solid #C0C0C0; " align="left" width="200" height="219"></font></b><font face="Verdana" size="1">StarcareMay 
					2005 was a watershed in the history of the utility sector of 
					Oman. That was when the unbundling of the ministry of 
					housing, water and electricity took place and the first 
					seeds for privatisation of the power and water sector were 
					sown. Wadi Al Jizzi Power Company (WAJPCO) was one of the 
					unbundled subsidiaries generation company along with Rusayl 
					Power Company; Al Ghubra Power and Desalination; and Rural 
					Areas Electricity System as well as distribution and power 
					and water procurement companies What also followed in this 
					period was that all the power plants became separate 
					entities and each had its own set priories of operational 
					challenges according to the business model. The operational 
					efficiency of the power plants then started to shape up and 
					reaching the international standards and reliability and 
					availability of power from these plants accordingly 
					significantly improved.<br>
					<br>
					It was only when the Electricity Holding Company decided to 
					sell the Rusayl Power Company to Suez Tractabel and 
					restructure the remaining two power plants (Wadi Al Jizzi 
					and Al Ghubra) did things begin to take better shape. Each 
					of the companies was placed under independent management 
					with express orders to rev up the efficiency of the plants.<br>
					<br>
					</font><font color="#000080" size="2" face="Verdana"><b>
					<br>
					<br>
					<br>
					<br>
					Challenging task<br>
					</b></font><font face="Verdana" size="1"><br>
					WAJPCO, situated in Wadi Jizzi in the wilayat of Sohar, 
					generating 297 MW of power fired with natural gas, was 
					handed over to Eng Zahir Khamis Al Bahri who became the 
					company’s general manager. Bahri was earlier the GM of Al 
					Ghubra and Rusayl plants, where he had achieved a good 
					record of reliability and implemented a successful programme 
					of Omanisation.<br>
					<br>
					“There were plenty of challenges when I took over as GM of 
					WAJPCO. The biggest job was to maintain the international 
					standards of operations to produce power apart from 
					complying with sectoral laws as well as those of the 
					ministry of environment and the ministry of civil defence,” 
					Bahri informed.<br>
					<br>
					Bahri also had an uphill task of developing the human 
					resources of the plant apart from increasing the 
					availability and reliability of power from the plant. At the 
					same time, he had to keep an eye on the balance sheet of the 
					company in order to increase net profits of the company.<br>
					<br>
					</font><font color="#000080" size="2" face="Verdana"><b>Team 
					work</b></font><font face="Verdana" size="1"><br>
					<br>
					If you ask how Bahri was able to overcame all these 
					challenges, he promptly points out that he believes in team 
					work and group delivery of the job. Further, you should 
					believe in people skills, he says. Explaining this, Bahri he 
					adds, “I had to open two fronts to streamline the operations 
					of the plant. The first was to improve individual competence 
					and the second was ensuring at the same time that this 
					individual was able to work in harmony and homogenously with 
					a team. This way, the staff had common goals and 
					achievements and they were able to talk the same language 
					which led to better understanding of the work.”<br>
					<br>
					Continuing further, Bahri says, “I can say very proudly that 
					from 2008, onwards, I was able to build a very good team in 
					my company and now most of the initiatives are now coming 
					from the staff itself. Now I can sit back and just play the 
					role of monitoring and as an overall incharge. I don’t 
					believe that one man can do everything. Any credit for 
					company achievement goes to the team. This is the only thing 
					that we should do – develop individual skills and hone their 
					capabilities.”<br>
					<br>
					The vast experience of Bahri in the power and water sector 
					came in handy during the crucial times. His first stint in 
					this sector began when he joined the ministry of housing, 
					electricity and water in 1992 as desalination project 
					engineer at the Sur desalination plant. Bahri was involved 
					in commissioning of the plant in coordination with 
					international consultants. The Sur plant with its reverse 
					osmosis technology later went on to become a reference point 
					in the whole of the Middle East as the most reliable plant 
					at that time.<br>
					<br>
					From there, Bahri became a section head of water department 
					and was later the director of production incharge of all the 
					North power stations and desalination plants. In 1998 he 
					returned from Scotland armed with an M.Sc and became the 
					section head for electricity and water in the directorate of 
					production, which was a wider body. Four years later, Bahri 
					became the director of production before becoming involved 
					in the unbundling process and later taking over as WAJPCO’s 
					GM.<br>
					<br>
					At WAJPCO, Bahri embarked on a policy of instituting systems 
					to usher in efficiency and reduce dependency on individuals. 
					When there is an efficient system, even if one person 
					remains absent, the work will go on, explains Bahri. The 
					company also introduced multi-tasking and cross-synergies so 
					that there was rotation of staff from one department to 
					another. To ensure that the staff and management are on the 
					same wavelength, WAJPCO conducts atleast two or three 
					training workshops per year for its middle level managers 
					outside the plant. The company’s plans and strategies are 
					reviewed, future course of action is discussed, and ideas 
					are welcome from everyone. “At WAJPCO, we have flat 
					integration, so there is no position but only ownership for 
					everyone,” says Bahri.<br>
					<br>
					</font><font color="#000080" size="2" face="Verdana"><b>
					Omanisation</b></font><font face="Verdana" size="1"><br>
					<br>
					Another notable aspect of WAJPCO is its reputation for a 
					high retention rate. Thanks to the exceptional HR policies 
					of the company and a very fair system of recognition within 
					the team, the company has become a very attractive place for 
					good talent. “When you have loyal staff, it makes the whole 
					growth and development of the company more streamlined. Our 
					company has become so attractive that many job applications 
					are pending with us. Besides this, WAJPCO has the highest 
					level of Omanisation in the power sector with 80 per cent 
					and we have the potential of increasing it to 85 per cent in 
					the next two years,” informs Bahri.<br>
					<br>
					We are also taken aback when we are told emphatically that 
					WAJPCO has also ‘Omanised’ its work. Explaining this, Bahri 
					says that that the maintenance and servicing work of the gas 
					turbines is very complicated and highly technical. In 
					addition, generally most power companies outsource this job 
					to the technical advisers of the OEMs. In the case of WAJPCO, 
					the staff felt that they are capable of handling this by 
					themselves without inviting any technical expert from 
					outside.<br>
					<br>
					</font><font color="#000080" size="2" face="Verdana"><b>
					Enviable record</b></font><font face="Verdana" size="1"><br>
					<br>
					They started with small jobs and the risk assessment showed 
					that indeed the plant’s staff could handle even the 
					complicated tasks as well. Finally, all those jobs done by 
					technical advisers flown in from abroad began to done 100 
					per cent by the WAJPCO’s staff.<br>
					<br>
					“This is the only power company in Oman doing all the major 
					maintenances works such as , combustion inspections (CI), 
					hot gas path inspections (HGPI), and other major 
					inspections. This year we will start the major 
					inspections(MI) of gas turbine on our own as our people are 
					very confident that they can do it without outside support,” 
					Bahri says.<br>
					<br>
					With this move, the company was able to actually achieve 
					better results, discloses Bahri. “We have gained more than 
					1.5 MW as a total proving that we can do it better. Usually, 
					the gas turbines have a degradation of at least 1 per cent 
					every year of its lifetime. However, instead of losing, we 
					gained 1.5 MW from our 11 turbines. This is big 
					achievement,” he says.<br>
					<br>
					Further WAJPCO was able to achieve another milestone of zero 
					LTI since the past two years. Reaching this important 
					highpoint in WAJPCO was not easy. Unskilled and semi-skilled 
					staff from the previous operation and maintenance company, 
					who were very weak in communication skills had to be trained 
					in the importance of health and safety environment through 
					group lectures and workshop. “Since all workers have a right 
					to work in a safe and healthy environment we drew up an 
					in-house policy on HSE to raise awareness and make sure they 
					understand the dangers at work. We made it clear that all 
					departments have to work together and work side by side. Now 
					we are aiming for zero LTI in the next three years also,” 
					Bahri says optimistically.<br>
					<br>
					Bahri expresses his gratitude to the company Board of 
					Directors who have provided a continuous direction and 
					support as well as the shareholders Electricity Holding 
					Company for their valuable support to the company. And we 
					can be certain that the shareholders will have many reasons 
					to cheer now as the efforts being put in by the management 
					and the staff towards operational efficiency has paid off. 
					WAJPCO, today, has managed to raise its reliability to 100 
					per cent and availability to 99.7 per cent in one month in 
					August.<br>
