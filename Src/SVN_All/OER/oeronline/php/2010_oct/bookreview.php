					by Ganesh Sundararaman</font></b></i></p>
				<p align="justify" style="margin-left: 10px; margin-right: 10px" dir="ltr"> 
                    <font face="Verdana" size="1">Much of Lords of Finance is 
					biographical. The lords of finance who constitute the title 
					of this book are the four central bankers who dominated the 
					postwar era: Benjamin Strong of the Federal Reserve Bank of 
					New York, Montagu Norman, the longtime head of the Bank of 
					England, Emile Moreau of the Banque de France, and Hjalmar 
					Schacht, who headed the Reichsbank. The decision to build 
					Lords of Finance around these four men is a brilliant 
					concept. Each of them was a powerful personality, with the 
					full range of strengths and weaknesses and insights and 
					eccentricities. Because much of the book concerns decisions, 
					like raising or lowering interest rates, there was a need 
					for great characters to pull the story along. Ahamed not 
					only has them, but also knows how to make them come alive.
					<br>
					<br>
					Strong is the domineering American; Schacht the arrogant, 
					headstrong German; Norman and Moreau prideful Europeans. 
					Strong and Norman became close friends, and their letters to 
					each other are a rich source of material, sometimes quite 
					touching.<br>
					<br>
					The central bankers were prisoners of the economic orthodoxy 
					of their time: the powerful belief that sound monetary 
					policy had to revolve around the gold standard. That is, 
					each country’s reserve bank had to have a certain amount of 
					gold in its vaults to back up its currency — and indeed, 
					“all paper money was legally obligated to be freely 
					convertible into its gold equivalent,” as Ahamed says. Again 
					and again, this straitjacket caused the central bankers — 
					especially Norman, gold’s most fervent advocate — to make 
					moves, like raising interest rates, that would allow their 
					countries to hold on to their dwindling gold supplies, even 
					though the larger economy desperately needed help in the 
					form of lower interest rates. <br>
					<br>
					</font><font color="#000080" face="Verdana"><b>Fast-paced</b></font><font face="Verdana" size="1"><br>
					<br>
					Who knew that a study of central bankers could be a 
					page-turner? Investment manager Liaquat Ahamed spins this 
					fast-moving yarn about central bankers’ disastrous monetary 
					policy decisions in the 1920s and early 1930s. The story 
					itself yields little suspense – you already know how it 
					ends, but Ahamed uses thorough research and gripping detail 
					to paint a complete picture of how the world economy 
					collapsed. <br>
					<br>
					The Great Depression preceded today’s credit default swaps, 
					collateralised mortgage obligations and arcane derivatives, 
					so there are many lessons for the modern crisis. There is no 
					attempt in Lords of Finance to present any macroeconomic 
					theory, however, and Ahamed concludes with a 
					non-explanation: “More than anything else, therefore, the 
					Great Depression was caused by a failure of intellectual 
					will, a lack of understanding about how the economy 
					operated.”<br>
					<br>
					This is a beautifully written book. Ahamed has a gift for 
					storytelling that most full-time writers would envy. Lords 
					of Finance is definitely for those who want a deeper 
					understanding of the gold standard, and the events that led 
					to – and out of – the biggest economic crisis of the 20th 
					century.<br>
