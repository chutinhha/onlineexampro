						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">
						For the discerning customer</font></b><p align="center" style="margin: 10px; ">
				<font face="Verdana" size="1">
				<img border="0" src="images/autotalk1-april2011.jpg" width="442" height="301" style="border: 1px solid #808080" class="style4"></font><p align="justify" style="margin: 10px; ">
				<font face="Verdana" size="1" class="style11" style="font-size: 11px">
				<b>The all-new Accent offers a comforting and exclusive driving 
				experience. Jacob George tests the sub-compact sedan in Dubai 
				for this report</b><br>
				<br>
				The all-new Accent began life as the automaker’s first product, 
				Pony, in 1976 and has grown to become one of Hyundai Motor 
				Company’s top-selling global models. The seventh-generation 
				Accent maintains its reputation with a refined new modern design 
				and an emphasis on high quality, while delivering improved fuel 
				efficiency and performance. Hyundai began developing the new 
				Accent in 2005 and the project cost is estimated at US$180mn 
				from concept to production. <br>
				<br>
				<b><font color="#C00000">Impressive dimensions</font></b> <br>
				<br>
				The new Accent continues Hyundai’s fluidic sculpture design 
				philosophy that injects sophistication and dynamic angles as 
				well as elegant lines resembling a calligrapher’s ‘orchid 
				strokes’ into the shape of a vehicle. <br>
				<br>
				Hyundai designers employed the design concept key words ‘Sleek 
				on Dynamic’ when crafting both the exterior and interior of the 
				new sub-compact sedan. With a coupe-like profile, the Accent’s 
				outer design is highlighted by dynamic and sophisticated graphic 
				elements, including eagle-eye two-tone bezel headlamps, unique 
				L-shaped fog lamps and a large hexagonal front grill. Its 
				refined and stately image is completed by sculptural side 
				character lines that start above the front fog lamps and extend 
				to the rear combination lamps. Inside the cabin where the 
				character lines flow continuously, a Y-shaped crash pad showing 
				bilateral symmetry gives a sense of stability. High-gloss black 
				and metallic colours used in the center fascia add a futuristic 
				and high-tech look to the interior of the Accent.<br>
				The new Accent is built on a newly-stretched wheelbase 2,570mm, 
				which is 70mm longer than the previous model, offering more 
				space and comfort. It gives the feel of a larger car for front 
				and rear seat passengers. Leg room for the front row is on a par 
				with that of a medium sized car: 1,112mm. It is also wider and 
				longer than its predecessor by 5mm and 90mm at 1,700mm and 
				4,370mm, respectively. However, the overall height has been 
				slightly reduced by 13mm to 1,457mm as the car sports a stylish 
				coupe-like profile without affecting head room. The new Accent 
				delivers class-leading luggage capacity that is wide enough to 
				fit four golf bags and three overnight bags. <br>
				<br>
				<font color="#C00000"><b>Enhanced powertrain</b></font> <br>
				<br>
				The sedan is powered by the new-generation Gamma engine, 1.6L or 
				1.4L DOHC in-line four-cylinder gasoline engines that feature 
				multiport fuel injection with CVVT (Continuously Variable Valve 
				Timing). By replacing the old Alpha engine of its predecessor 
				with Gamma, the sedan promises more power and torque, as well as 
				improved fuel economy, in keeping with Hyundai’s pledge to be 
				the industry’s eco-leader. The power and torque of 1.6 Gamma are 
				respectively 124ps and 15.9 kg•m, while those of 1.4 Gamma are 
				108ps and 13.9 kg•m. The powerful and fuel-efficient Gamma 
				engine will be mated to a four-speed automatic transmission or a 
				standard five-speed manual transaxle.<br>
				<br>
				The high-tensile strength steel that comprises 58.6 per cent of 
				the new sub-compact sedan’s floor and chassis offers enhanced 
				crash performance as well as reduced weight. The Accent’s 
				underlying structure is also engineered to protect occupants 
				using a Collision Load Dispersion mechanism that distributes the 
				force of a frontal impact in three ways to minimise floor 
				deformation and maintain the integrity of the cabin, while a 
				ring-type structure in the centre pillar area provides more 
				strength and protection against side impact. In addition, 
				Hyundai offers Anti-lock Brake System (ABS) and an optional 
				six-airbag system to further upgrade safety. <br>
				<br>
				The long wheelbase of the Accent contributes to a smooth ride 
				with enhanced stability. McPherson Struts on the front wheels 
				provide fully independent suspension with sensitive steering and 
				controlled manoeuvrability. The old style sub-frame has been 
				replaced by an H-shaped type, for a simpler and lighter 
				structure. In the rear, a Coupled Torsion Beam Axle (CTBA) is 
				applied to optimise the suspension and shock absorption in line 
				with the changing situation on the road. Motor Driven Power 
				Steering (MDPS) gives precise handling, sharp steering response 
				and improved fuel efficiency as well.</font></td>
