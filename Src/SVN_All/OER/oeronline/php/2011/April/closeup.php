						<font face="Verdana" size="4" color="#C00000">
						Stronger GDP of GCC<br>
&nbsp;</font></b><p align="justify" class="style13">
				<strong><span class="style14">GCC countries have emerged as 
				prominent global players with their combined GDP crossing the 
				trillion dollar mark<br>
				</span></strong><br>
				The combined gross domestic product (GDP) of the Gulf 
				Cooperation Council (GCC) countries is projected to reach a 
				record $1.1tn in 2011, up from $993bn in 2010. The credit for 
				this should be given to the steady government spending on the 
				back of strong oil prices and crude output. According to the 
				IMF, this figure is in real terms and adjusted to inflationary 
				pressures, clearly suggests a higher value in nominal terms.<br>
				This marked development adds to the profile of GCC states on the 
				international front in terms of reputation and prominence. The 
				World Bank figures for 2009 ranks the GDP of Saudi Arabia on the 
				basis of purchasing power parity at number 21 globally, putting 
				the size of Saudi economy ahead of numerous European countries 
				such as Belgium, Sweden and Switzerland.<br>
				<br>
				<b>Steady spending</b><br>
				<br>
				Fiscal spending accounts for more than a quarter of GDP of the 
				GCC states- and more in some of the member states. All regional 
				countries opted for increasing public sector spending for fiscal 
				year 2011 on the back of projected oil prices. Stronger public 
				sector spending helps these countries overcome the lingering 
				repercussions of the global financial crisis which erupted in 
				2008. The crisis undermined economic confidence leading 
				governments to assume a leadership role in overcoming the 
				debacle. Besides, private sector investors in the GCC countries 
				tend to follow suit.<br>
				For example, Saudi Arabia’s budget for fiscal year 2011 stands 
				at $154bn, up by 7 per cent compared to the original figure for 
				2010. However, this figure is based on assumed oil prices, 
				suggesting the possibility of further increase in the total 
				expenditures thanks to stronger oil prices. Projected 
				expenditures amount to $21.1bn, up 4 per cent from budgeted 
				expenses for fiscal year 2010.<br>
				<br>
				<b>Higher oil prices</b><br>
				<br>
				Sticking to an-age old conservative fiscal approach, GCC 
				countries used relatively low oil prices whilst preparing 2011 
				budgets, with Bahrain being a rare exception this time around. 
				Needless to say, the income from the petroleum sector 
				constitutes at least three quarter of the state exchequer in the 
				GCC states. Hence, steady oil prices create the opportunity for 
				more government spending which eventually leads to further 
				economic prosperity.<br>
				<br>
				For instance, Oman assumed an average oil price of $58 per 
				barrel for fiscal year 2011, considerably lower than prevailing 
				market prices. Chances are that average oil prices could hover 
				around $90 per barrel in 2011 partly owing to the market 
				reactions to socio-political problems in numerous MENA countries 
				like Libya.<br>
				<br>
				For its part, Bahrain prepared the budget for fiscal years 2011 
				and 2012 based on oil price average of $80 per barrel. However, 
				it is believed the trend reflects the desire for limiting 
				projected deficit by increasing income amidst growing spending. 
				Certainly, extraordinary deficit causes problems for the 
				country’s credit rating and consequently borrowing cost. <br>
				<br>
				<b>Energy output</b><br>
				<br>
				The third cause for stronger economic results for GCC in 2011, 
				besides steady spending and firm oil prices, is the ability to 
				increase the output of energy sources. In fact, Saudi Arabia, 
				the world’s largest oil exporter, traditionally plays the role 
				of augmenting oil output in case of a possible drop in 
				production due to various reasons including political turmoil. 
				It is believed that the kingdom had spare capacity in excess of 
				one third of its actual production prior to the unrest in Libya, 
				an OPEC member boasting a crude output of around 1.6 million 
				barrels per day. And the Saudis proved they were prepared to 
				make up for the drop of Libyan output by increasing the 
				production.<br>
				<br>
				Qatar leads the world in the production of natural liquefied gas 
				(NLG). Only recently the country celebrated reaching LNG output 
				capacity of 77 million tonnes per annum. As such, Saudi Arabia 
				and Qatar lead the world in the export of crude oil and LNG, 
				respectively.<br>
				<br>
				Still, GCC states are particularly noted for controlling 
				substantial petroleum resources including 23 per cent of global 
				oil production. Still, GCC states account for 41 per cent of 
				proven oil reserves in the world. Saudi Arabia which leads other 
				countries in terms of both export and reserves of crude oil 
				alone accounts for more than 19 per cent of worldwide proven oil 
				reserves. <br>
				<br>
				Kuwait and the UAE hold 7.5 per cent and 7.2 per cent of 
				globally proven oil reserves, respectively. In addition, GCC 
				countries hold 23 per cent of proven gas reserves in the world. 
				Qatar ranks number three in the world after Russia and Iran in 
				terms of stock of gas reserves.<br>
				<br>
				<b>Global appreciation</b><br>
				<br>
				The prominence of GCC countries is ever growing on the global 
				stage partly due to a stronger GDP. The combined value of the 
				GCC economies has crossed the trillion dollar mark. <br>
				<br>
				The world looks up to GCC countries for help in times of crisis. 
				For instance, it expects them to increase oil output in critical 
				times, thereby avoiding substantial rise in oil prices because 
				of the shortage. Excessively high oil prices stand to damage 
				economic well-being throughout the world, with adverse effects 
				reaching the shores of GCC countries through higher export 
				prices imposed by oil importing countries.<br>
