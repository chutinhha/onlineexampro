				<img border="0" src="images/beyondboardrooms1-april2011.jpg" width="150" height="175" style="border: 1px solid #808080; float: right;" class="style4"></font><font size="1"><b>Can 
				you give us a brief background on your professional experience?<br>
				</b><br>
				I have worked with Khimji Ramdas Watches Division (KRWD) for the 
				last 14 years. I joined the company in 1997 as a sales executive 
				and have risen through the ranks to the position of retail and 
				marketing manager. It has been a very fulfilling journey as I 
				have grown both personally and professionally in my job and 
				within the organisation. I thank the company for recognising, 
				appreciating and rewarding my work aptly. I have been lucky to 
				have a number of mentors within the organisation like Madhu 
				Jesrani, general manager, KRWD, Vivek Pande CEO- Luxury and 
				Lifestyle group and Rajeev Ahuja, head of corporate 
				communications who have guided and taught me a number of things.
				<br>
				<br>
				In 2004, the company gave me an opportunity and supported me to 
				do my Masters in Business Administration. This helped me build 
				up my confidence at a higher level, enabling me to improve my 
				performance within the organisation.<br>
				<br>
				KRWD has been like a family for me. It has been very considerate 
				and co-operative all the time. It has been very supportive in 
				case of any health or family related emergencies. KRWD has about 
				20 employees out of which 10 are women, both Omani as well as 
				expatriates. Under my current position, I have a team of eight 
				members working with me. I train them and learn from them on a 
				day to day basis.<br>
				<br>
				The directors of the KR Group, Anil Khimji and Pankaj Khimji, 
				are very friendly and approachable. Their doors are always open 
				for us as they encourage discussions on a one to one basis.<br>
				<br>
				<b>You have been in the luxury marketing business for a long 
				time.What are the essentials for marketing such high-end 
				products?</b> <br>
				<br>
				KRWD is a franchisee for some of the world’s renowned Swiss 
				watches and jewellery brands such as Rolex, Cartier, Chopard etc 
				along with exclusive mobile phones from Vertu. Overseeing the 
				sales and marketing of such brands requires a complete knowledge 
				and information of the brand, product range and pricing. In 
				addition, one needs to understand the customers’ requirements, 
				needs and desires. Certain customers also ask for an appropriate 
				presentation and this varies on a case-to-case basis. This 
				company encourages us to upgrade our skills by sending us for 
				various training sessions which has enhanced our confidence to 
				serve our clients better. <br>
				<br>
				<b>
				<img border="0" src="images/beyondboardrooms2-april2011.jpg" width="223" height="323" align="left" style="border: 1px solid #000000">What is your advice to other women who are looking at 
				charting out a successful career?<br>
				</b><br>
				My message to all working women and aspiring women is simple 
				–Keep up to your interest. <br>
				<br>
				Have an inclination to learn new things, regardless of the ups 
				and downs in your personal and professional life. <br>
				<br>
				Strike a balance between your professional and family life. <br>
				<br>
				Get a good education, think positive and work to achieve success 
				in your job.<br>
