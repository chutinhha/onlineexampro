						Sound and stable</font></b><p align="justify" class="style13">
				<b><font size="7">A</font></b>Google search for the world’s safest banks throws up 2.51 
				million searches. The global financial crisis needs to be 
				credited for making safety and stability of financial 
				institutions a prime concern. Despite new regulatory measures 
				and investor protection campaigns, over 140 banks failed in the 
				US in 2010. Mercifully, the Sultanate’s banking sector has been 
				insulated from such adversity. The Central Bank’s prudent 
				measures have ensured a stable environment.<br>
				<br>
				Aggregate deposits of commercial banks as at the end of December 
				2010 increased by 14.6 per cent to RO10,516.8mn. Total 
				outstanding credit rose by nine per cent to RO10,724.3mn. The 
				OER-GBCM Best Banks in Oman Survey 2011 reflects the soundness 
				of the sector and the strengths and challenges faced by 
				individual banks.<br>
				<br>
				The Central Bank has given permission for one new commercial 
				bank to start operation in Oman, and this should add to the 
				vibrancy of the sector. The overall macroeconomic outlook for 
				Oman remains positive and this is expected to gather momentum in 
				2011. Commercial banks may experience pick up in credit demand. 
				As the deposit growth was impressive in 2010 and the liquidity 
				conditions continued to remain comfortable, commercial banks may 
				not find it difficult to meet all genuine credit demands of the 
				economy. <br>
				<br>
				Speaking about stability, the wave of protests, strikes and 
				demonstrations taking place in the Sultanate since the last week 
				of February has created an uncertain environment. The reforms 
				carried out to address the demand of protestors are expected to 
				have a steep impact on the finances of the private sector and 
				the government’s budgetary deficit. Reported estimates peg the 
				losses at over $1bn. We hope that reason prevails and things 
				settle down soon because the prevailing uncertainty will surely 
				have an adverse impact on business.<br>
