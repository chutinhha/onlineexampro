						<p align="justify" style="margin: 3px 10px"><strong>
						<span class="style27">Ensuring quality and safety</span></strong><p class="style15">
				<strong><span class="style14">In medical tourism lurks the 
				danger of much needed resources for domestically available 
				services being siphoned off and diverted overseas<br>
&nbsp;</span></strong><p class="style15" style="text-align: center">
				<img border="0" src="images/health1-april2011.jpg" width="400" height="260" style="border: 1px solid #808080"><p align="justify" class="style13">
						<p align="justify" class="style13">
				Demands on public health systems worldwide continue to rise with 
				burgeoning waiting lists for non-urgent interventions and 
				greater numbers of frustrated and dissatisfied patients. In some 
				countries there is increasing collaboration with the private 
				sector health providers to tackle the problem. Such is the 
				situation in Oman where discussions are underway in an attempt 
				to relieve the increasing burden on the public health services.<br>
				<br>
				<b>Booming private sector</b><br>
				<br>
				Members of the public, however, continue to be enticed with 
				claims of cheaper healthcare overseas under the label of medical 
				tourism. The trade is booming in the private sector of a number 
				of countries, with many providers jumping on to the bandwagon. 
				The fear is that entrepreneurial initiatives might outweigh 
				consistency in the quality of the care given.<br>
				<br>
				A couple of simple questions should be posed. Firstly, is it 
				really cheaper? The price quoted for overseas treatment packages 
				may appear to be very competitive, but may not be all inclusive. 
				Many people have returned from such trips having incurred far 
				more costs than expected. There is also the “cost” in terms of 
				stress from the separation of the patient from his or her 
				family. It is well recognised that recuperation from any medical 
				intervention is enhanced in the presence of support from the 
				family. Presently the government in Oman offers financial 
				support in whole or in part, to nationals travelling overseas 
				for treatment. Historically, this initiative was put in place to 
				provide medical services that were not available in the home 
				country. Over the intervening years the government in Oman has 
				invested heavily in healthcare, including more hospitals, 
				improved services and an increase in the numbers of home grown 
				medical school graduates, thereby increasing medical manpower. 
				Despite this, the government continues to expend large sums of 
				money annually to meet the cost of overseas treatment even 
				though the treatment may now be available in Oman. This is 
				amplified in cases where family members accompany the patient 
				(especially in the case of minors). There is a danger therefore 
				that much needed resources for domestically available services 
				will be siphoned off and diverted overseas. <br>
				<br>
				<b>Medical malpractice</b><br>
				<br>
				Secondly, is the treatment overseas better than that available 
				at home? The two driving forces in patient care are quality and 
				safety. In most western societies these parameters are 
				regulated, with audits of patient outcome being commonplace. 
				Such a strategy remains inconsistent in some of the providers of 
				medical tourism. They may still be third world countries where 
				you would not even risk drinking the water. One trademark of 
				quality care is the plan for follow up and handover of clinical 
				information to your doctor back home. Such after sales service 
				can frequently be lacking. At times too, complications occur and 
				frequently land at the feet of the doctor in the home country 
				who has little information on what took place overseas. If in 
				the extreme there has been medical malpractice, the recourse 
				available for the patient and their family is severely inhibited 
				when treatment has taken place overseas.<br>
				<br>
				<b>Role of private sector</b><br>
				<br>
				Of course for some, part of the problem is the ever growing 
				backlog for procedures in the public sector hospitals and 
				patients are naturally asking the question “why do I have to 
				wait for treatment?” Private sector health providers can play an 
				important part in reducing such backlogs and providing care in a 
				more timely fashion. Patients and their families will also have 
				an improved choice of where to have their treatment carried out. 
				Muscat Private Hospital (MPH) has established its reputation as 
				a leading provider in the private health sector and has in place 
				quality-driven internationally recognised evidence based 
				practices across the organisation. This is demonstrated by the 
				hospital’s commitment to obtain formal accreditation by the 
				Joint Commission International.<br>
				<br>
				The hospital now provides a 24 hours acute care service at its 
				location on Bausher Road. Furthermore, the facilities are 
				focussed on the provision of customised care plans in which an 
				individual who presents himself with a specific set of symptoms 
				will be seen by a multidisciplinary team of senior doctors, 
				allied health professionals and nursing staff. Examples include 
				the Heart Centre, Breast Centre, Emergency Room (ER), Women’s 
				Health Service and the Sports Injuries Centre. These pathways 
				are supported by a comprehensive round-the-clock laboratory 
				service and state-of- the-art imaging from the Allied 
				Diagnostics Centre (ADC). In addition to low-dose x-rays it 
				provides imagery from ultrasound, Computerised Tomography (CT) 
				and Magnetic Resonance Imaging (MRI). It also has the only 
				digital mammography machine in the whole of Oman.<br>
				<br>
				The hospital is also looking at innovative ways in which the 
				larger corporations based in Oman can improve access to quality 
				heath care for their employees. Health screening packages are 
				already in place and the opportunity exists for such 
				organisations to negotiate treatment packages on behalf of their 
				workers.<br>
				<br>
				Continuity of care is guaranteed at MPH with a good “after care 
				service” and allegiance to its motto of “Family First”. The 
				hospital believes in offering a value for money service to the 
				community, with very competitive pricings for the multitude of 
				procedures performed. Before embarking on an expensive, often 
				stressful overseas trip, check first with MPH. You might be very 
				surprised at what can be offered in the way of a quality, safe, 
				value service from a centre of repute.</td>
