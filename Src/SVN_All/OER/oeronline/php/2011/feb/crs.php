						<font face="Verdana" size="4" color="#C00000">Batting 
						for the game</font></b><p align="justify" class="style3">
				<strong><span class="style5">Oman Cricket’s coach Iqbal Sikander 
				says Oman has the potential to become an international cricket 
				venue, thanks to the support from corporate Oman. An OER report<br>
				</span></strong>
				<br>
				<font face="Verdana" size="1">
				<img border="0" src="images/feb-csr1.jpg" width="200" height="252" style="border: 1px solid #808080; float: right;" class="style4"></font><font size="1">For 
				the development of any sport, the support of corporate entities 
				is seen as an essential part. In a place like Oman, where 
				everyone mostly talks football, the sport of cricket is just 
				about getting a foothold. The relegation of the cricket team to 
				Division III after finishing at the 11th place in the 2009 ICC 
				World Cup qualifier in South Africa did not help matters much.<br>
				<br>
				Enter Iqbal Sikander, ex-Pakistani cricketer, and an Asian 
				Cricket Council (ACC) development officer, as the coach of the 
				national team. His mandate -- to use his skills both as a 
				cricketer and as an administrator to give the team a strategic 
				direction. While talking about his new responsibility as coach 
				of Oman Cricket team, Sikander thanks business houses in Oman, 
				which have pledged their support to the national team.<br>
				“I was pleasantly surprised to see that there are so many CEOs 
				and managers who swear by cricket and that is very encouraging 
				and good for the development of the game. Yes, corporate support 
				is very much needed if the game has to flourish in the country,” 
				Sikander says.<br>
				<br>
				Sikander’s responsibility in ACC is to develop the game of 
				cricket among the Asian countries in terms of infrastructure, 
				the administration and to help national teams, which are in 
				second and third divisions. The International Cricket Council 
				permitted him to take up the responsibility to coach the Oman 
				Cricket team and help them to get to Division II. The World 
				Cricket League Division II, was underway even as OER went to 
				print.<br>
				<br>
				The people and the companies with whom Sikander has interacted 
				with so far have been cooperative and helpful and they have been 
				able to raise around $1mn for Oman Cricket.<br>
				<br>
				Says Sikander, “The good thing about Oman Cricket is that the 
				board of directors are all working in big corporations or they 
				are owners of big businesses so they are able to support Oman 
				Cricket in a big way. This is a plus point for cricket in Oman.”<br>
				<br>
				Sikander, continuing his effusive praise of the corporate 
				support says, “All the board members love cricket and of what I 
				know, they eat, drink and sleep cricket. It is fantastic. Even 
				in the board meeting, everyone is talking cricket, not just Oman 
				Cricket but world cricket. I would like to especially thank the 
				Khimji Ramdas family for supporting the &#8232;game of cricket.”<br>
				<br>
				The ACC is committed to providing a quarter of a million dollars 
				for building two cricket grounds and $50,000 for a 
				state-of-the-art flood-lit cricket coaching academy at the 
				Sultan Qaboos Sports Complex in Bausher. The ministry of sports 
				has allocated land for the two grounds in Amerat and has given 
				an in-principle approval for the coaching academy as well. One 
				of the two cricket grounds will be developed as an international 
				ground and the day is not far off when Oman can become a neutral 
				venue for countries like Pakistan to play their test and ODI 
				matches.</font></td>
