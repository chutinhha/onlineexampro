						<font face="Verdana" size="4" color="#C00000">
						Social spending is the key</font></b><p class="style15">
				<strong><span class="style14">Oman’s 2011 budget, the largest in 
				the country’s history, is yet another measured step in the 
				direction of economic and social development</span></strong><p align="justify" class="style13">
				When Pakistan-based economist Mahbub ul Haq devised and launched 
				the Human Development Index in 1990 he stated that its purpose 
				was, ‘‘to shift the focus of development economics from national 
				income accounting to people centered policies.’’ The Human 
				Development Index (HDI) is a composite statistic used to rank 
				countries by the level of ‘human development’ and separate 
				developed, developing, and underdeveloped countries. The 
				statistic is composed of data on life expectancy, education and 
				per-capita gross national income. The HDI has redefined the way 
				development is looked at. If conventional economics approached 
				development through the narrow prism of per capita income and 
				gross national income, governments and finance ministers are now 
				judged on their ability to improve the standard of living of 
				their countrymen. Literacy rates, hospital beds per thousand, 
				internet penetration and employment generation are as important 
				as industrial production, deficits and earnings for measuring 
				growth.<br>
				<br>
				Judged against this backdrop, Budget 2011 and the 8th Five Year 
				Development Plan stand out for the importance being accorded to 
				social spending. Education, health and employment generation 
				have all got top dollar allocation. Education gets a budgetary 
				allocation of RO926mn, six per cent more than 2010. The health 
				outlay has similarly gone up by 14 per cent, reaching RO335mn. 
				Social security and housing outlays too have witnessed a sharp 
				increase. These will foster inclusive economic growth. <br>
				<br>
				The 8th plan widens job opportunities for the national workforce 
				by providing 200,000 to 275,000 new jobs over the next five 
				years. This will surely go a long way in fulfilling the 
				employment aspirations of young Omanis. The plan aims to further 
				the private sector’s role in national economy by stimulating 
				domestic and foreign private investments and raising the private 
				sector’s share in GDP. This is good news for the corporate 
				sector. <br>
				<br>
				Visvas Paul D. Karra, assistant editor and someone who has been 
				tracking the annual budget for years says, “It is a growth 
				oriented budget but more needs to be done to diversify the 
				economy in pursuance of Vision 2020.” Readers can catch his 
				incisive views on Budget 2011 and the 8th Plan in this edition’s 
				cover story.</td>
