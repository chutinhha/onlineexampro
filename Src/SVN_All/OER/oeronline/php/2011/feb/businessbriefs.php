				to 2010. The Legal Encyclopedia (LE) includes Royal decrees 
				issued since the beginning of the Renaissance and the decisions 
				related to the regulation of telecom affairs. The LE shows and 
				documents the royal decrees since the enactment of the wireless 
				telegraphy law in 1973, followed by specifying the functions of 
				the ministry of post, telegraphs and telephones in 1979 and 
				transforming the general telecommunications organisation into an 
				Omani closed joint stock company in 1999, and finally issuing 
				the Telecommunications Regulatory Act in 2002 which crowned the 
				march of telecommunications in the Sultanate and laid the 
				foundations of liberalising the telecom sector. The Encyclopedia 
				also contains all telecommunication resolutions and the 
				necessary amendments to telecommunications services for all 
				beneficiaries. The aim of the LE is to highlight the legal 
				aspects that were involved in regulating the telecommunications 
				sector and the various stages of legislative development.</td>
