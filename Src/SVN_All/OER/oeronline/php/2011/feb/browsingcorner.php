						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">Growing 
						divide</font></b><p align="justify" class="style3">
				<strong><span class="style5">The Winner Take-All Society delves 
				into the reasons for increasing income disparity and the need 
				for a more egalitarian society<br>
				</span></strong>
				<br>
				<font face="Verdana" size="1">
				<img border="0" src="images/feb-brows.jpg" width="200" height="267" style="border: 1px solid #808080; float: right;" class="style4"></font><font size="1">Disparity 
				amongst the richest strata and the rest has been a favourite 
				hunting ground for economists for decades. In the US the top 5 
				per cent of households have increased their share of the 
				national income from 17 per cent to 21 per cent since 1978. The 
				average income of the top 20 per cent of Americans has grown by 
				about one-third while the average income of those in the middle 
				20 per cent has been stagnant.<br>
				<br>
				The authors contend that it is the nature of job markets to give 
				top performers huge rewards and everyone else crumbs. For 
				example, Michael Eisner makes $200mn in a single year as CEO of 
				Disney; novelist Danielle Steel gets $12mn per book; boxer 
				Evander Holyfield is paid $28mn for a year of prizefights. 
				Meanwhile, the people in the median income strata struggle to 
				better their quality of life. <br>
				<br>
				<strong>Innate advantage<br>
				</strong><br>
				Frank and Cook put forward several reasons why a 
				winner-takes-all market is emerging now, all of a sudden? The 
				biggest reason is technology as it gives gifted people a chance 
				to reach out to a large audience. The best opera singer 100 
				years ago may have earned the equivalent of $1mn a year. Her 
				income was sharply limited by physical constraints – the number 
				of seats in the theaters she could tour in a single season. 
				Today, however a pop star can make $50mn a year since he can 
				reach hundreds of millions of fans in just one night by 
				satellite and sell them his mass-produced CDs the next morning.<br>
				<br>
				A second reason for the rise of this phenomenon is what the 
				authors call ‘decision leverage’. As corporations have grown 
				larger, write Frank and Cook, “a small difference in the quality 
				of even a single decision can translate into an enormous 
				difference in the value of the final output. Consider a CEO who 
				must decide which of two new products will be produced by his 
				Fortune 100 firm making the right choice could easily mean 
				several million dollars of added profit.” Ease of 
				transportation, weakening of regulations and erstwhile taboos 
				and a self perpetuating process in which one success fuels more 
				successes are other factors that account for a winner take-all 
				society. <br>
				<br>
				The markets for which the authors have the strongest evidence of 
				‘winner take all’ characteristics are presented in the earlier 
				part of the book. As the book goes on, however, the authors keep 
				reiterating the same thesis without taking it forward or giving 
				credible evidence to back their claims. <br>
				<br>
				At times they even admit that the evidence for winner-take-all 
				characteristics in a particular industry or service is 
				untenable. While the book takes off on a high note it leaves the 
				reader a trifle disappointed by the end.</font></td>
