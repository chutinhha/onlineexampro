						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">
						What next in emerging markets?</font></b><p class="style15">
				<strong><span class="style14">Country selection will be crucial 
				in while picking stocks on Asian bourses in 2011</span></strong><p align="justify" class="style13">
				After an epic two-year rally that saw indices in India, Russia, 
				Brazil, Indonesia, South Korea and several small countries 
				double since March 2009, emerging markets face a significant 
				correction in 2011. Why? One, the monetary tightening of India 
				and China will accelerate as the People Bank of China (PBOC) and 
				RBI, Asia’s flagship central banks, are forced to respond to 
				food/energy inflation with rate hikes and shrinkage in money 
				supply/bank loan growth. This means the bull run in Shanghai and 
				Mumbai is over for now. <br>
				<br>
				Two, US GDP growth has begun to rise, making it unnecessary for 
				the Bernanke Fed to expand its balance sheet. This means the 
				dollar and US Treasury debt yields will move higher, a negative 
				for commodities and emerging markets. Three, the Eurozone 
				sovereign debt crisis has returned with a vengeance, as even 
				China and Japan scramble to buy Portuguese debt to preempt a 
				market assault on Spain. Four, emerging market debt is 
				overvalued and the post-Lehman spread compression is over. Five, 
				high input cost will hit corporate operating earnings.<br>
				<br>
				The importance of country selection will be critical in 2011, as 
				it was in the GCC last year. After all, Qatari shares rose 25 
				per cent in 2010 while some UAE blue chips lost 20 per cent. 
				Qatari bank QNB rose by more than 60 per cent while some 
				construction companies/property developers lost a third of their 
				value in Kuwait and Dubai. I believe country selection will be 
				crucial in Asian equities. Avoid China, Indonesia and India; buy 
				Taiwan, Malaysia, South Korea and Singapore on a correction.<br>
				<br>
				<strong>Inflation surge<br>
				</strong><br>
				China’s Politburo is haunted by the inflation surge that 
				preceded both Chairman Mao’s establishment of People Republic in 
				1949, the chaos of the Cultural Revolution in the 1960’s and the 
				Tiananmen Square massacre in June 1989, the three seminal events 
				in the history of the Middle Kingdom. A society where half of 
				the CPI index is food and pork and where 600 million peasant 
				farmers struggle to survive without the Maoist iron rice bowl, 
				is naturally vulnerable to riots, social unrest, and even urban 
				revolt. This is the reason why the Chinese central bank raised 
				banking reserve ratios six times in 2010, hiked bank lending 
				rates and slashed bank loan growth quotas. Yet Chinese consumer 
				inflation has risen from 4.4 per cent in October to 5.1 per cent 
				in November despite the biggest monetary squeeze since the 2008 
				recession. There is no way Premier Wen Jiabao’s 3 per cent 
				inflation target can be met in 2011, as global food, iron ore 
				and steel prices rise and the rural transport infrastructure is 
				hit by the floods in Schezwan. The 20 per cent hike in minimum 
				wages, Chinese New Year and food inflation mean a 6 per cent CPI 
				is inevitable.<br>
				<br>
				So PBOC tightening will continue and the risk of a Chinese hard 
				landing will no longer be non-trivial. This has ill omens for 
				the Chinese stock market, with another 20 per cent downside 
				possible in the Shanghai index. China is, alas, well behind the 
				inflation curve and, despite modest valuations, Shanghai shares 
				could see earnings downgrades. I expect an emerging market 
				mini-meltdown that will not spare the Gulf, crude oil prices or 
				Wall Street once the financial markets understand that China is 
				now moving into the twilight zone of tight money. The bull could 
				well be goring in Shanghai in the Year &#8232;of the Rabbit.<br>
				<br>
				The Indonesian stock market will naturally be vulnerable to a 
				Chinese hard landing, as the central bank in Jakarta must 
				tighten interest rates when foreign investors flee to defend the 
				rupiah and combat inflation. It is no coincidence that Indonesia 
				dropped 4 per cent in the first two weeks of 2011 and the rupiah 
				has fallen to seven month lows against the dollar. Indonesia, 
				after all, is the world’s most high beta Asian emerging market 
				and grossly overvalued. The IMF has recommended that Bank Negara 
				Indonesia must raise rates. Believe me, they will in 2011. This 
				means Indonesian shares are an accident waiting to happen.<br>
				<br>
				<strong>Behind the curve<br>
				</strong><br>
				A similar risk reward calculus lies behind my caution on Indian 
				shares, despite the 3,000 point correction in Sensex since 
				October. As food inflation surges, the RBI is behind the curve 
				despite successive monetary tightening policies. India is still 
				the most expensive major emerging market in the world at 18 
				times earnings at a time when the spectrum telecom scandal has 
				raised political risk in New Delhi and increased the risk of a 
				foreign capital exodus in Asia’s most crowded, consensus trade. 
				Indian banks have also begun to underperform the Sensex as 
				funding costs rise and loan growth slows. Indian consumer shares 
				likes Nestle India or ITC are unjustifiably expensive at above 
				30 time earnings. Property markets face a potential crash as 
				monetary tightening accelerates as the RBI responds to 
				stratospheric global crude oil, food and metal prices. Sensex 
				could well fall to 16,000 in 2011 with downside risk in the 
				Indian rupee to 47 against the dollar on a potential FII exodus 
				from Dalal Street.<br>
				<br>
				South Korea’s Samsung Electronics is one of the world’s most 
				successful high technology conglomerates, a huge benefit of the 
				global smartphone, tablet and 3D/smart TV offerings. Unlike its 
				peer Apple in Silicon Valley, Samsung Electronics is cheap at 11 
				times earnings and 1.6 times book value. I believe Samsung 
				offers a 25-30 per cent return in 2011 though with a buy/sell 
				trading range between 800,000 won and 1.2mn won.<br>
				<br>
				<strong>Crown jewel of growth<br>
				<br>
				</strong>Singapore Telecom (Singtel) is the crown jewel of 
				Southeast Asian growth investing, despite recent share price 
				weakness due to its investment in Bharti Airtel and Australia’s 
				Optus. However, a major catalyst will be the rollout of the 
				national broadband network in Singapore and the exponential 
				surge in mobile traffic. Singtel is not expensive at 12 times 
				earnings, and offers a 5.6 per cent dividend yield. Singtel is a 
				buy at or below three Singapore dollars for a 3.50 target even 
				as it uses Bharti’s margin woes to increase its stake.<br>
				<br>
				Though I am currently negative on the Sensex, I believe a huge 
				money making opportunity exists in Coal India, though only below 
				INR 280 for a 350-370 target. Coal India is the world’s largest 
				lowest cost coal producer and excess Indian demand will allow it 
				to increase output as power utilities sales and global exports 
				surge. Coal India can well deliver 15-18 per cent earnings 
				growth for the next three years. India’s coal deficit will only 
				rise in 2011 though the post-IPO euphoria has eliminated its 
				valuation discount to global peers. While I am also attracted by 
				the fall in ICICI Bank ADR from 58 to 44 in New York (symbol 
				IBN), I believe investors should wait till 35 to buy as RBI 
				rates pressure the bank. </td>
