						<span class="style27">Abiding commitment</span></strong><p class="style15">
				<strong><span class="style14">The Environmental Management 
				System (EMS) of Shangri-La’s Barr Al Jissah Resort and Spa in 
				Muscat seeks to reduce the impact of its daily operations on the 
				surrounding flora and fauna</span></strong><p align="justify" class="style13">
				Shangri-La Hotels and Resorts has long been involved in 
				initiating green hotel initiatives in all of its hotels and 
				resorts around the world. The implementation of ISO14001 has 
				been a mandatory goal for all of its worldwide properties. 
				Therefore, at the initiation stage of any of its resort 
				projects, strong measures are taken to adhere closely to the 
				local government legislations with regards to environment 
				conservation. Shangri-La’s Barr Al Jissah Resort and Spa in 
				Muscat has also developed and implemented an Environmental 
				Management System (EMS) to identify the main areas where the 
				daily operations of the resort could have an adverse impact on 
				the surrounding flora and fauna. The project documentation was 
				submitted for the certification of ISO14001 standard.<br>
				<br>
				<strong>Innovative strategies <br>
				</strong><br>
				Setting up smart goals and objectives, through constant 
				operational surveillance and strict compliance to set guidelines 
				in Environmental Management Systems (EMS), the hotel tried to 
				ensure that it can meet the targets to become ISO14001 
				certified. The programme enjoyed the full support of the hotel 
				management. In order to apply innovative methods and strategies, 
				initial audits were carried out to identify possible 
				environmental impacts, which in turn led to the development of 
				Environmental Management Plans (EMPs) and Best Practice 
				procedures which are followed on a daily basis. Over 30 
				departments across the resort have implemented Best Practices to 
				monitor and improve the effectiveness of operational control 
				measures. More than 1,000 Best Practices are in place within the 
				resort such as the reduction of water, gas and electricity 
				usage, controlling disposal of waste products, paper recycling, 
				reduction of unnecessary printing and photocopying, switching 
				off lighting, air-conditioning, computers and other 
				electricity-powered items when not in use. <br>
				<br>
				The hotel has both ISO and CSR steering committees, which 
				conduct weekly meetings to promote continuous awareness among 
				all hotel colleagues, update on initiatives being carried out 
				and to identify any potential new areas in the environment where 
				existing methods and strategies could be applied to contribute 
				to the conservation. As many as 14 of the hotel’s employees are 
				officially certified auditors and accredited by URS.<br>
				<br>
				<strong>With optimism<br>
				</strong><br>
				The effectiveness of the programme can really be proven over 
				time. For example, a huge reduction in the use of paper will 
				ultimately reduce the number of vehicles travelling to the 
				resort, and in turn, reduce vehicle fuel consumption. The 
				actions will, in effect, have a chain reaction down the line to 
				the suppliers and manufacturers of items. </td>
