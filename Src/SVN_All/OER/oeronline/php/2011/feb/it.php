						<span class="style27">Breaking new ground</span></strong><p class="style15">
				<strong><span class="style14">A strong player in the ICT sector 
				in the Middle East over the last 20 years, CNS is looking to 
				impart a dynamic and forward thinking approach to the industry 
				in the Sultanate. Excerpts from an interview with Mufaddal Nafar, 
				Area Sales Manager, Oman CNS</span></strong><p class="style15">
				&nbsp;<p align="justify" class="style13">
				<font face="Verdana" size="1">
				<img border="0" src="images/feb-it1.jpg" style="border: 1px solid #808080; float: right;" class="style28"></font><strong>Give 
				a brief description of CNS and its operations.<br>
				<br>
				</strong>CNS has developed and enhanced its operations more in 
				the last two years than any other time in our history. We are 
				breaking new ground with a dynamic and forward thinking approach 
				to ICT, defining how it can be best deployed and managed. We are 
				known for our trusting, innovating and enterprising approach.<br>
				<br>
				<strong>When was the Oman branch of CNS opened and how has it 
				been faring thus far?<br>
				<br>
				</strong>OmanCNS was launched at the COMEX 2010 based out of 
				Knowledge Oasis Muscat. It has been involved in exciting 
				projects and it has completed some key projects in the country 
				so far while a number of other major projects are being 
				executed.<br>
				<br>
				<strong>What are the innovative products and services launched 
				by CNS?<br>
				<br>
				</strong>OmanCNS offers turnkey solutions on ICT Infrastructure 
				and managed services. It is a premier partner of Oracle Hardware 
				&amp; Exadata Solutions in the country. It is one of the first in 
				Oman to offer Remote Operations Availability Management (ROAM). 
				CNS also offers green ICT technology solutions, data centre 
				design, build-operate-manage and many other high tech solutions.<br>
				<br>
				<strong>What are the major projects that have been done by CNS 
				so far, both in Oman and UAE?<br>
				<br>
				</strong>Major projects in Oman include design build and manage 
				data centres, virtualised environments and thin client 
				computing. OmanCNS also has delivered consulting assignments in 
				design and re-engineering of data centres.<br>
				<br>
				<strong>Can you give some details about CNS’s green activities?<br>
				<br>
				</strong>It is estimated that the CO2 emissions of the ICT 
				industry alone exceeds the carbon output of the entire aviation 
				industry. The ICT industry has a collective responsibility to 
				help address this problem. Fortunately CNS has the experience at 
				hand to reduce energy consumption, reduce our dependence on non 
				sustainable fuel sources and to reduce CO2 emission.<br>
				<br>
				<strong>What is the growth being experienced by the ICT sector 
				in the region and how does CNS expect to cash in on this?<br>
				<br>
				</strong>CNS has been a strong player in the ICT sector over the 
				last 20 years in the Middle East. Oman is witnessing some strong 
				growth and with the government initiatives supporting the ICT 
				sector in general, CNS expects to deliver some quality projects 
				which enhance value of investments in ICT by the customer.<br>
				<br>
				<strong>What are the latest trends in the IT sector?<br>
				<br>
				</strong>Increasing compute capabilities of computers, including 
				mobile devices, along with improving connectivity, is enabling a 
				shift in how businesses support operational decisions. It is 
				becoming possible to run simulations or models to predict the 
				future outcome, rather than simply provide backward-looking data 
				about past interactions, and to do these predictions in real 
				time to support each individual business action.<br>
				<br>
				Another latest trend is that of context-aware computing which 
				focuses on the concept of using information about an end user or 
				object’s environment, activities, connections and preferences to 
				improve the quality of interaction with that end user.<br>
				<br>
				In the coming third wave of computing, computers are invisibly 
				embedded into the world. As computers proliferate and as 
				everyday objects are given the ability to communicate with RFID 
				tags and their successors, networks will approach and surpass 
				the scale that can be managed in traditional centralised ways. 
				This leads to the important trend of imbuing computing systems 
				into operational technology, whether calming technology or 
				explicitly managed and integrated with IT.<br>
				<br>
				A fabric-based computer is a modular form of computing where a 
				system can be aggregated from separate building-block modules 
				connected over a fabric or switched backplane. In its basic 
				form, a fabric-based computer comprises a separate processor, 
				memory, I/O, and offload modules (GPU, NPU, etc.) that are 
				connected to a switched interconnect and, importantly, the 
				software required to configure and manage the resulting 
				system(s).<br>
				<br>
				The next three years will also see the delivery of a range of 
				cloud services that fall between the extremes of private versus 
				public. Suppliers will offer packaged private cloud 
				implementations that deliver public cloud service technologies 
				(software and/or hardware) and methodologies (i.e., best 
				practices to build and run the service) in a form that can be 
				implemented inside the enterprise.</td>
