						<td valign="top">
						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">
						Looking ahead</font></b><p class="style15">
				<strong><span class="style14">Oman’s 2011 budget, the largest in 
				the country’s history, is yet another measured step in the 
				direction of economic and social development</span></strong><p align="justify" class="style13">
				Those unacquainted with Oman’s fiscal history might be forgiven 
				for viewing Oman’s 2011 budget with surprise. The budget, which 
				was announced on January 2, is the largest in the Sultanate’s 
				history, with RO8.13bn ($21.1bn) in spending. The deficit, too, 
				may strike some as out of character for a country with a 
				reputation for cautious fiscal policies: with RO7.28bn in 
				expected revenues, the government is, at least on paper, 
				expected to fall short by RO850mn ($2.2bn) or nearly 12 per cent 
				of revenues.<br>
				<br>
				Furthermore, Oman’s eighth five-year development plan – the 
				second to last in the government’s ‘Vision 2020’ development 
				programme – calls for RO42.71bn ($109bn) in government spending 
				over the 2011-2015 period. Much of this will be channeled into 
				the education and health sectors: the government has committed 
				to a 55 per cent increase in spending on education-related 
				infrastructure and services, and an 88 per cent increase in 
				health spending. The government has budgeted for an annual 
				deficit of approximately 13 per cent through the five-year plan.<br>
				<br>
				<strong>A shift in policy?</strong><br>
				<br>
				Oman has become known as a stable destination for foreign 
				investment because its spending plans, while generous, were 
				seldom described as risky. Are these larger-than-ever 
				appropriations the sign of a shift in policy by the Omani 
				government? The simple answer would be no. As anyone who follows 
				fiscal policy in the Gulf countries will know, the projected 
				deficit is in line with previous years’ projections, which have 
				consistently undershot the expected revenues from oil sales.<br>
				<br>
				The 2010 budget, for example, was figured with a projected oil 
				price of $50 a barrel. With the actual oil price over the year 
				averaging out at $76, the government was able to cover its 
				projected deficit and even pay off some long-term development 
				loans early, according to minister of national economy HE Ahmed 
				bin Abdulnabi Macki. The 2011 budget plans for an average oil 
				price of $59, which is an extremely conservative estimate given 
				that prices have climbed to $90/barrel as of mid-January.<br>
				<br>
				An oil revenue windfall could wipe out the projected deficit, 
				but it would bring its own worries, principle among them 
				inflation. As Oman’s currency is pegged to the US dollar, the 
				country’s central bank has little latitude to adjust interest 
				rates to curb inflationary pressure.<br>
				<br>
				The risk of government demand outpacing supply and driving up 
				prices is real. The record oil prices of 2008 saw inflation soar 
				to 12.6 per cent, prompting the government to increase the wage 
				for public-sector workers by 43 per cent and pensions payments 
				by 5-35 per cent in an effort to maintain consumer demand. This 
				puts significant pressure on the private sector to increase 
				wages, an action which was unrealistic for many companies facing 
				the first wave of the financial crisis. <br>
				<br>
				<strong>Inflationary pressures<br>
				</strong><br>
				While 2009 saw a return to more modest inflation, at 3.5 per 
				cent, rates increased again in 2010, to 4.35 per cent. The 
				planned increase in government spending, combined with the 
				increasing interest of investors tired of low returns in 
				traditional markets, could push this up further in 2011. The 
				Omani government will have to be proactive to address the 
				challenge presented by these inflationary pressures.<br>
				<br>
				Fortunately, it has already taken steps in this regard. Some of 
				the surplus will no doubt be funneled into the State General 
				Reserve Fund (SGRF), which was created in 1980 to absorb budget 
				surpluses and provide a reserve fund for deficits. In large part 
				because of the SGRF, Oman has never defaulted on its foreign 
				debt or been forced to seek a rescheduling agreement. Filtering 
				potential surplus into the SGRF, rather than further increasing 
				spending, will help keep inflation in check.<br>
				<br>
				<strong>On a diversified path</strong><br>
				<br>
				This isn’t to say the already budgeted increases in spending 
				aren’t good investments. On the contrary, Oman’s young and 
				growing population – 62 per cent of the population is under the 
				age of 30 and only 7.8 per cent of the population is over the 
				age of 50 – will benefit immensely from expanded health and 
				education offerings. Indeed, as the 2011 budget marks the 
				kickoff to the penultimate five-year plan, the appropriations 
				are key to the Sultanate’s vision of setting the economy on a 
				diversified and sustainable path by 2020. <br>
				<br>
				While hydrocarbon revenues have played an important role in the 
				country’s development, investment in the oil and gas sector is a 
				better generator of hard capital than human capital. Exploration 
				and extraction relies on the expertise of a limited number of 
				skilled technicians, often from abroad. And with Oman’s reserves 
				due to run out in the next 20-30 years, it is an investment with 
				a finite return. <br>
				<br>
				The increased appropriations for the education and health 
				sectors, by contrast, represent the kind of investment that has 
				the capacity to drive wealth creation in the country. These 
				sectors are both high employers that equip Omani citizens with 
				the tools – health and knowledge – to become productive members 
				of society. <br>
				<strong><br>
				Employment programmes</strong><br>
				<br>
				As the details of the specific programmes due to be funded in 
				2011 become public, it is to be hoped that the educational 
				offerings will include employment-oriented programmes that 
				provide vocational training and leadership development. Oman has 
				done a good job of encouraging entrepreneurship through 
				programmes like Injaz Oman, which sponsors a yearly competition 
				for student-led businesses. Through the organisation, teams of 
				students are matched with corporate volunteers, who mentor the 
				students through the process of setting up a small company.<br>
				<br>
				Developing skills like this could go a long way to generate 
				employment for Oman’s youth. Globally, small and medium-sized 
				enterprises, or SMEs, are estimated to employ four times as many 
				people as larger companies. They are also generally considered 
				to be excellent vehicles to drive innovation and competition in 
				developing economies. <br>
				<br>
				In an effort to encourage the development of SMEs, a number of 
				private and public sector entities established funds to make 
				loans to entrepreneurs between 2000 and 2009.<br>
				<br>
				However, many of these initiatives have been scaled back because 
				of the number of defaults on the loans. It is now acknowledged 
				that not enough support infrastructure and advising capabilities 
				were put in place to aid entrepreneurial development. Investing 
				in training programmes like Injaz Oman can go a long way to 
				prepare prospective entrepreneurs with the necessary skills to 
				run a successful business.<br>
				<br>
				The government’s ability to conceive of, implement, and maintain 
				a cohesive long-term development plan has been a decisive factor 
				in the country’s ongoing modernisation, and it should come as no 
				surprise that the 2011 budget, however high, represents less of 
				a radical step than the numbers would at first suggest. Indeed, 
				it is yet another measured step in the direction of economic and 
				social development. </td>
