						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">
						Proactive fiscal spending in Bahrain</font></b><p align="center" style="margin: 10px; ">
				<font face="Verdana" size="1">
				<img border="0" src="images/feb-closeup.jpg" style="border: 1px solid #808080" class="style4"></font><p align="justify" class="style13">
				<strong><span class="style14">In Bahrain, solid spending by the 
				public sector will help achieve desired economic growth rates 
				and create more job opportunities<br>
				</span></strong><br>
				Authorities in Bahrain seem determined to deal with the 
				lingering repercussions of global financial crisis, as evidenced 
				by the steady rise of spending in fiscal years 2011 and 2012. 
				This is the right step, as numerous entities including the IMF 
				and G-20 consider stronger spending as the best possible means 
				for overcoming the crisis, in turn erupting in mid-2008.<br>
				<br>
				In fact, solid spending by the public sector would encourage 
				private sector investors to follow suit. Like elsewhere within 
				the Gulf Cooperation Council (GCC) region, investors in 
				conservative Bahrain look up to the public sector for leadership 
				during the time of crisis.<br>
				<br>
				<strong>Two-year budget<br>
				</strong><br>
				Bahrain releases the budgets for two fiscal years concurrently, 
				this time for 2011 and 2012. This practice is purportedly 
				designed to provide stakeholders details of economic directions 
				via public sector spending, which comprises more than a quarter 
				of the country’s gross domestic product (GDP).<br>
				<br>
				However, the practice faces various risks including volatility 
				concerning a key source of income, namely the petroleum sector. 
				Bahrain is a price taker by virtue of being a relatively small 
				producer. Combined onshore oil output amounts to 180,000 barrels 
				per day. Bahrain and Oman are the two GCC countries who are not 
				members of the OPEC.<br>
				<br>
				<strong>Largest budget<br>
				</strong><br>
				Projected figures for fiscal years 2011 and 2012 are the 
				highest. For fiscal year 2011, total expenditures and revenues 
				are put at nearly $6.8bn and $5.8bn, respectively. This leaves 
				behind a projected deficit of $1bn. The statistics for 2012 
				point out to spending of about $7.1bn and income of $5.9bn and 
				thus a shortfall of $1.2bn.<br>
				<br>
				To be sure, projected expenditures for fiscal year 2011 exceed 
				budgeted figure for fiscal 2010 by 9 per cent and actual 
				spending for 2009 by 19 per cent. This is the right step, as 
				steady public sector spending can help achieve desirable 
				economic growth rates on the one hand and create jobs on the 
				other.<br>
				<br>
				Reflecting stronger government spending, projections call for 
				the GDP to grow between 4 to-5 per cent in 2011. This level of 
				economic growth rate should help authorities deal with 
				emplunemployment, a socio-economic problem facing Bahrain.<br>
				<br>
				Bahrain violates one criteria of the Gulf Monetary Union (GMU) 
				notwithstanding stronger budgetary revenues on the back of 
				firmer assumed oil price. Projected deficits are noteworthy 
				anyway as it comprises 4.1 per cent and 4.6 per cent of the GDP 
				in 2011 and 2012, respectively. Launched at the start of 2010, 
				the GMU restricts budget deficit to 3 per cent of the GDP.<br>
				<br>
				<strong>Surprising estimates<br>
				</strong><br>
				The authorities calculated an average oil price of $80 per 
				barrel both in 2011 and 2012, thus double the rate assumed in 
				2009 and 2010. Yet, this level of oil price is in line with the 
				market rate of around $90 per barrel at the start of 2011. In 
				reality, oil prices are forecast to remain solid during 2011 due 
				to various reasons including growing economic confidence, 
				especially in Asian countries like China and India and the 
				adverse weather conditions in different parts of the world.<br>
				<br>
				Still, Bahrain needs an average oil price of $97 per barrel in 
				2011 and $100 per barrel in 2012 in order to reach the 
				equilibrium level, with no deficit whatsoever. Conversely, 
				assuming a relatively low price of $58 per barrel like that of 
				Oman would raise projected deficits considerably and damage the 
				country’s credit rating.<br>
				<br>
				<strong>Maintaining credit rating<br>
				</strong><br>
				In late 2010, both Standards &amp; Poor’s and Fitch re-affirmed 
				their outstanding credit ratings for Bahrain. For its part, 
				Standard &amp; Poor’s reaffirmed Bahrain’s credit rating at A with 
				stable economic outlook. Similarly, Fitch Ratings affirmed A for 
				Bahrain’s long-term foreign issue and maintained a stable 
				outlook.<br>
				<br>
				The moves by Standard &amp; Poors’ and Fitch followed an earlier 
				rating by Moody’s Investor Services which downgraded Bahrain’s 
				sovereign rating and provided a negative outlook of the 
				country’s financial services sector. More specifically, Moody’s 
				downgraded Bah- rain’s sovereign rating by a notch from A2 to A3 
				for reasons concerning financial stability. Amongst others, 
				breakeven point has increased from $30 per barrel in 2004 to $80 
				per barrel in 2008.<br>
				<br>
				Another restraint on Bahrain’s rating is the the exposure of 
				banks to weak domestic and regional real estate sectors. To be 
				sure, the financial services sector is uniquely important by 
				virtue of compromising nearly a quarter of Bahrain’s GDP. The 
				financial crisis of 2008 compounded problems facing the 
				country’s real estate. Yet, steady governmental spending in 2011 
				and 2012 will be helpful in addressing some concerns raised by 
				Moody’s.<br>
				<br>
				<strong>Oil dependency<br>
				<br>
				</strong>On a negative side, the petroleum sector accounts for 
				87 per cent of total income in both fiscal years, confirming the 
				significance of the hydrocarbons sector despite claims of 
				economic diversification. Bahrain generates its petroleum 
				revenue from sale of crude oil from both offshore and onshore 
				fields and gas. Abu Saafa field, which is shared with Saudi 
				Arabia, accounts for 80 per cent of total oil revenue. Bahrain’s 
				own onshore field and sale of gas account for 10 per cent 
				apiece.<br>
				<br>
				The remaining 13 per cent of the revenue are generated from the 
				sale of utilities, customs on imports, return on investments and 
				grants extended by other countries. There is no doubt that 
				Bahrain has to go a long way to lessen its dependence on the 
				petroleum sector.</td>
