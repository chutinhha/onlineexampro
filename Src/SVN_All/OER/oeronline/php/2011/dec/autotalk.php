						<b><br>
						The driver-focused V8 Vantage S from Aston Martin 
						features enhanced dynamics, increased power and torque 
						coupled to a compelling package of agility, balance, 
						engagement and everyday practicality, writes Visvas Paul 
						D Karra after an exhilarating test drive</b></font><p align="center" style="margin: 3px 10px">
						<font face="Verdana" size="1" class="style11" style="font-size: 11px">
				<br>
						</font><font face="Verdana" size="1">
				<img border="0" src="images/autotalk_dec1.jpg" width="450" height="321" style="border: 1px solid #808080" class="style4" vspace="8"></font><p align="justify" style="margin: 3px 10px">
						&nbsp;<p align="justify" style="margin: 3px 10px">
						<font class="style11">
						<font style="font-size: 11px" face="Verdana" size="1">
						When you talk about an Aston Martin, you immediately 
						think of James Bond and his inimitable action packed 
						adventures – more specifically the high speed car chases 
						with 007 at the wheel of a modified Aston Martin.<br>
						<br>
						That youthful excitement invariably kicks in when you 
						take the wheel of any Aston Martin. And the Aston Martin 
						V8 Vantage S is no different. The moment you push the 
						ignition button located in the middle of the dashboard, 
						the engine rumbles to life with a roar that is 
						unmistakably Vantage. And the knowledge that the V8 
						engine has a new ground-up developed seven speed 
						Sportshift II transmission means that the seven short 
						ratio gears will deliver the most engaging and intense 
						driver involvement.<br>
						<br>
						The makers of Aston Martin say the Vantage range has 
						come of age with the new V8 Vantage S. There is no doubt 
						about it as it is characterised by a host of new 
						driver-focused features all designed to push the honed 
						dynamics of the standard Vantage to a new benchmark 
						level.<br>
						<br>
						The 4.7 litre V8 engine has been engineered to deliver 
						peak power of 430 bhp (436 PS / 321 kW) at 7300 rpm and 
						torque of 490 Nm (361 lb.ft) at 5000 rpm. The engine is 
						willing, the handling is predictable and the controls 
						are easy so it is as comfortable to cruise on the city 
						roads as it is on winding roads. It has a maximum speed 
						of 305 km/h tempting you to throttle the engine on 
						straight roads in the country side.<br>
						<br>
						Even at high speeds, the Vantage S offers immense 
						stopping power for absolute confidence thanks to a 
						competition derived floating disc front brake system 
						which is complete with larger discs and six-piston 
						calipers with a more responsive but heavier steering 
						rack and sporting suspension settings.<br>
						<br>
						</font><font color="#000080" size="2" face="Verdana"><b>
						Enhanced feeling</b></font><font style="font-size: 11px" face="Verdana" size="1"><br>
						<br>
						At the core of the Vantage S is its seven speed 
						Sportshift II automated manual transmission, offered as 
						standard and delivers exceedingly rapid gear changes. 
						The extra gear permits shorter well-spaced ratios taking 
						advantage of the optimum torque, which combined with a 
						shorter final drive ratio of 4.182:1 delivers quicker 
						acceleration and an enhanced sporting feel. In addition, 
						the Vantage S features a ‘Sport’ button which quickens 
						the gear changes and gives the driver a more aggressive 
						throttle response while also opening the exhaust bypass 
						valves across most of the engine speed range perfectly 
						characterising the ‘S’ variant. All this ensures 
						exceptional in-gear acceleration for a searing sense of 
						urgency. <br>
						<br>
						The Vantage S benefits from its own unique sound with a 
						new exhaust muffler and bypass valve map, creating a 
						very distinctive and highly vocal exhaust note. The 
						lyrical quality of the engine sound is music to the ears 
						as its gains speed.<br>
						<br>
						The Vantage S conveys agility and performance. From the 
						front, a new lower front bumper finished in 
						perfectly-aligned carbon fibre houses a larger air 
						intake feeding the engine and front brakes. The splitter 
						combined with the extended deck lid ‘flip’ work in 
						harmony to provide increased down force at speed. New 19 
						inch ‘V’ spoke wheel styles are available as standard, 
						while an optional 10-spoke lightweight forged wheel 
						option, reduces unsprung mass further. A new rear bumper 
						and side sills optically widen the car.<br>
						<br>
						</font><font color="#000080" size="2" face="Verdana"><b>
						Aesthetic appeal</b></font><font style="font-size: 11px" face="Verdana" size="1"><br>
						<br>
						The plush leather interiors, the push buttons for the 
						various drive modes, the sat-nav and all the other knobs 
						and buttons are neatly set into the dashboard to give 
						you the luxurious and seamless feeling. All in all the 
						V8 Vantage S epitomises Aston Martin’s approach to 
						design: A commitment to engineering excellence without 
						aesthetic compromise. Each model shares the same 
						beautiful form, undeniably modern yet with a direct 
						lineage to some of the great Aston Martin models of the 
						past.<br>
						<br>
						As its name and race-bred looks suggest, the V8 Vantage 
						S is a more overtly sporting Vantage aimed at the 
						keenest driver, heavily influenced by the 2011 Vantage 
						GT4 race car. Available as a coupe or a roadster, 
						styling changes include new wheels and race-winning 
						aerodynamics honed in GT racing, while inside unique 
						detailing creates a very special driving ambience.</font></font><br>
