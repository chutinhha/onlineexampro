						How has GLOREI’s performance been in Oman during the 
						last year?</strong><strong style="font-weight: 400"><br>
						We have many upcoming projects in the residential, 
						commercial, hospitality, logistics and other sectors. 
						GLOREI’s shareholders are a group of leading financial 
						and investment companies in the Gulf. There is a clear 
						strategic vision consistent with the founders’ 
						commitment to participate in the economic development of 
						the GCC countries in general, and Oman in particular, 
						taking the real estate development sector as the base of 
						our investments. GLOREI is moving ahead at a good pace 
						which is coupled with high financial capabilities, a 
						strong network of strategic partners and affiliates, a 
						team of highly qualified professionals, and certain 
						basic principles and values to undertake its day-to-day 
						operations and development processes, these are 
						professional honesty and equity in our dealings, 
						providing healthy working environment conducive to 
						creativity and invention, active participation in 
						community development and preservation of a clean 
						environment.<br>
						<br>
						</strong><strong>
						What are your current projects and what is your outlook 
						for the future?</strong><strong style="font-weight: 400"><br>
						In the hospitality sector, GLOREI started the 
						development of a five-star hotel -Shaza Muscat Hotel 
						which is coming up next to the Tilal Al Khuwair complex 
						in the Al Khuwair area of Muscat. It is about 16 km away 
						both from the Muscat International Airport and the 
						Central Business District (CBD) in Greater Muttrah. In 
						addition, GLOREI has the first Business Centre in Muscat 
						with full services, the modern and sophisticated 
						business centre (Maktabi Al Khuwair) which is located in 
						the heart of the city of Muscat in the prestigious 
						business district and diplomatic area. Another project 
						which marks GLOREI’s expertise in commercial office 
						spaces is an environmentally friendly commercial 
						project- Maktabi Al Watiyah. Apart from all these, the 
						first large-scale development project of the company is 
						to be unveiled soon; it is an integrated real estate 
						development that includes residential and commercial 
						units in Wilayat Bausher.<br>
						<br>
						</strong><strong>
						What differentiates you from other development and 
						investment companies?</strong><strong style="font-weight: 400"><br>
						Our core business is real estate development and 
						investment and our investments have till date covered a 
						range of real estate projects- commercial, residential, 
						hospitality, mix-use, logistics. We have yet to see 
						another company that has achieved so much diversity in 
						such a short span of time with such consistency. Our aim 
						is to expand our project portfolio to cover different 
						geographical areas of Oman.<br>
						<br>
						</strong><strong>
						What made you to partner Oman Debate 2011?</strong><strong style="font-weight: 400"><br>
						GLOREI’s participation in the Oman Debate will provide 
						us the opportunity to actively contribute in the areas 
						related to investment and development sector and to be 
						proactive in raising topics affecting the wider sector 
						across Oman and the whole region. Also, through our 
						wider experience, we will be providing valuable inputs 
						into discussions related to our sector.<br>
						<br>
						</strong><strong>
						What are your expectations from Oman Debate 2011?</strong><strong style="font-weight: 400"><br>
						The event will provide an excellent opportunity for 
						GLOREI to determine the importance of various set of 
						issues and hot topics which focus on how to cope up with 
						the developments of the Arab Spring. The value of our 
						contribution would come through meeting of the finest 
						experts coming from around the region. <br>
