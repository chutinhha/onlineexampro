						<font face="Verdana" size="4" color="#C00000">Moulding 
						green generation<br>
&nbsp;</font></b><p align="justify" style="margin: 3px 10px">
				<b><font size="2" face="Verdana">Teaming up with Oman Green 
				Awards, Nomac Oman has embarked on a new green initiative to 
				collect used papers and plastic from schools in order to send 
				them for recycling. Muhammed Nafie reports<br>
				<br>
				</font></b><font size="1" face="Verdana"><br>
				<img border="0" src="images/env_dec1.jpg" style="border: 1px solid #808080" class="style4" hspace="8" vspace="4" width="150" height="154" align="left">As 
				a sequel to Oman Green Awards (OGA), the first Environmental 
				Awards in the Sultanate, a new initiative for paper and plastic 
				recycling at schools was launched recently in Barka by Nomac 
				Oman, a subsidiary of ACWA Power International. In keeping with 
				OGA’s established goal to motivate behavioural change and 
				increase awareness to protect and preserve the environment, the 
				initiative seeks to encourage students to make use of recycling 
				as an effective way to reduce their carbon footprint. <br>
				<br>
				As the first step of the initiative, Nomac Oman handed over 
				recycled drums to Al Amal Secondary School in Barka in order to 
				collect waste papers and plastic from the school premises. The 
				company will replace the drums once it is filled to the 
				capacity, and send the waste papers for recycling. The recycling 
				will be done from Dubai through Muscat-based Aman Company. 
				Around 50 students accompanied by two teachers from the school 
				visited the desalination plant at ACWA Power Barka. This was 
				followed by two informative presentations for students on 
				desalination and paper recycling led respectively by Talal Al 
				Mabsali, operation and maintenance manager, Nomac Oman and Fahad 
				Ibrahim Al Kindi, team leader HSE, Nomac Oman. The initiative 
				not only seeks to get the school rid of used papers and plastics 
				getting accumulated in large quantities every day at its 
				premises, but also envisions to mould a new generation of 
				students deeply informed and conscious about environmental 
				issues.<br>
				<br>
				“Recycling is one of the best ways for us to have a positive 
				impact on the world in which we live. Recycling is important to 
				both the natural environment and us. We must act fast as the 
				amount of waste we create is increasing all the time,” says 
				Fahad. “Since we do not have recycling facilities in Oman right 
				now, we need to send them to Dubai. Nomac Oman will replace the 
				drums once they are filled and take them to Aman Company which 
				will get it recycled from Dubai. If the project proves to be 
				successful we are looking to expand it to other schools and to 
				the society as a whole,” he explains.<br>
				<br>
				During an interaction with students he stressed the importance 
				of making the new generation more serious about the enormous 
				ecological impact of rampant manufacturing of paper on an 
				industrialized scale. “The procurement and processing of raw 
				materials have a variety of negative effects on the environment. 
				Denudation of woodlands and exhaustion of water supplies are 
				some of these effects. As the demand for paper has increased, 
				more timber has been needed to meet the demand for wood pulp. In 
				some cases this has meant the loss of valuable wildlife habitats 
				and ecosystems, as old forests have been replaced by managed 
				plantations, usually of fast-growing conifers. The lack of tree 
				species diversity in managed forests has a direct impact on the 
				biodiversity of the whole forest,” he added. </font>
						<p align="justify" style="margin: 3px 10px">
