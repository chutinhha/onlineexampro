				<font size="1" face="Verdana">
				<img border="0" src="images/env_dec2.jpg" style="border: 1px solid #808080" class="style4" hspace="8" vspace="4" width="450" height="195"><br>
&nbsp;</font><p align="justify" style="margin: 3px 10px">
				<font size="1" face="Verdana">Zainab Ahmed Al Balushi, teacher 
				at Al Amal School Barka, said that she was very optimistic about 
				the initiative because it could collect the papers and the 
				plastic the school throws out each day and send them for 
				recycling. In addition, it will help create awareness among the 
				students and inculcate the habit of recycling among them. “Being 
				part of UNESCO Associated Schools Project – ASPnet, our school 
				already has different plans to educate students on issues 
				related to UNESCO’s overarching goal of promoting peace and 
				international understanding,” she said. “It includes several 
				initiatives for sustainable development such as preserving water 
				for the next generation. We are also part of an initiative for 
				preserving water by Life-link Friendship School. They sent us 
				guidelines and we gathered information on household water 
				consumption in our area and sent the results. We also went to 
				Jordan to do the presentation and discuss the results. It is 
				aimed at making the students aware of water consumption and 
				encourage them not to waste the water.”<br>
				<br>
				The students from Al Amal School consider this initiative as a 
				good beginning towards awakening the eco-consciousness of the 
				students. Maryam Saud exuded confidence saying that the new 
				initiative will set the tone for a good behavioural change in 
				treating used paper and plastic not only among the students but 
				also in the society as a whole. Nada Ahmed and Mathar Mohammed, 
				students from the same school, said youngsters can do a lot of 
				things to make the society more informed about environment 
				conservation. They highlighted the importance of conducting case 
				studies in different areas in order to create awareness in 
				society about the seriousness of this matter. <br>
				<br>
				Oman Green Awards have been created to honour outstanding 
				environmental vision, endeavours and achievements of corporate 
				and individuals who matter most as they strive in this 
				environmental cause. Nomac Oman which runs the operation and 
				maintenance of ACWA Power Barka attaches greater importance to 
				protect the environment by formulating an effective carbon 
				management <br>
				strategy and plan.<br>
