						<p align="justify" style="margin: 3px 10px">
						<font face="Verdana" size="4" color="#C00000"><b>South 
						Africa beckons<br>
						<br>
						</b></font><font face="Verdana" style="font-size: 9pt">
						<b>Following the Football World Cup in 2010, South 
						Africa has become a centre of attraction both for 
						business and pleasure. Omani investors should cash in on 
						this opportunity, says HE Yusuf Saloojee, South African 
						Ambassador in an interview
						</b></font><i>
						<font color="#333333" face="Verdana" size="1">with 
						Visvas Paul D Karra
						</font></i><font face="Verdana" style="font-size: 9pt">
						<b><br>
						</b></font><font face="Verdana" size="1"><i>
						<font color="#333333"><br>
						</font></i><br>
				<img border="0" src="images/sainterview_dec1.jpg" style="border:1px solid #808080; " class="style19" align="right" width="200" height="268">The 
						Vuvuzela, one of the most endearing symbol of Football 
						World Cup held in South Africa in 2010, filled the 
						stadiums with its loud and raucous sound reflecting the 
						exhilaration of supporters. It wouldn’t be off the mark 
						to say that the echo of this sound horn was heard all 
						over the world including Oman. <br>
						<br>
						“After the World Cup, things have not been the same in 
						South Africa. Tourism boomed, businesses grew and even 
						the global financial crisis has had a minimal impact on 
						our country,” says HE Yusuf Saloojee, South African 
						Ambassador to the Sultanate. More importantly, Omanis 
						are now eyeing this beautiful country not only for 
						visiting as tourists but also for businesses.<br>
						<br>
						The visit of South African President Jacob Zuma has come 
						at the most opportune time as it gives a tremendous 
						boost to the economic and bilateral trade relations 
						between Oman and South Africa, says HE Saloojee.<br>
						<br>
						“We have always been enjoying friendly relations with 
						Oman. On the international front, Oman and South Africa 
						are both part of the Indian Ocean Rim, Non-Aligned 
						Movement etc. We want to translate those good relations 
						into something concrete which will help to enhance the 
						trade between both our countries,” says HE Saloojee. 
						Diplomatic relations between Oman and South Africa were 
						established in 1994 after South Africa’s first 
						democratic elections but bilateral trade and investments 
						relations have been sagging for various reasons.<br>
						<br>
						Oman presents an abundance of opportunities for South 
						African businesses to get involved and at the same time, 
						South Africa also offers many lucrative opportunities 
						for growth and investments. Currently, some of the areas 
						are infrastructure development, agriculture, mining and 
						beneficiation, manufacturing, the green economy and 
						tourism. There are also opportunities for collaboration 
						in the areas of education and training, science and 
						technology and agriculture, he says.<br>
						<br>
						</font><font face="Verdana" size="2" color="#000080"><b>
						Partnership Oman<br>
						</b></font><font face="Verdana" size="1"><br>
						One of the big developments that took place on the 
						bilateral front was the visit of HE Yusuf bin Alawi, 
						Oman’s minister Responsible for Foreign Affairs to South 
						Africa in 2010. There, HE Alawi signed an agreement 
						known as Partnership Forum with HE Maiti Mashabane, 
						South African Minister for International Relations and 
						Cooperation. The agreement covered several areas of 
						cooperation including trade, economic development, arts 
						and culture, higher education, transport and 
						agriculture.<br>
						<br>
						In that meeting, it was also agreed that the first 
						meeting of the Partnership Forum would be held in 
						Muscat, which indeed took place in October, 2010. The 
						South African delegation to Oman was led by Ibrahim 
						Ibrahim, deputy Foreign Minister of South Africa and the 
						two sides worked out a number of details and identified 
						areas of mutual cooperation. The next meeting would be 
						held in South Africa in 2012.<br>
						<br>
						According to HE Saloojee, there is no point signing 
						agreements unless there is follow-up and implementation. 
						That is precisely why visit of the South African 
						President Zuma came close on the heels of that October 
						meeting. During the two day visit on November 15-16, HE 
						Zuma held a meeting with His Majesty and visited the 
						Port of Sohar. In a speech delivered at the Business 
						Forum of South Africa and Sultanate of Oman held at Al 
						Bustan Palace Hotel, HE Zuma said the continent of 
						Africa and the Sultanate of Oman have relations that 
						date back to ancient times when traders from both 
						regions were using the maritime routes along the Indian 
						Ocean rim to trade their goods.<br>
						<br>
						“I am truly pleased to be in Oman on a state visit, at 
						the kind invitation of His Majesty the Sultan. I was 
						pleased to have had an opportunity to discuss a number 
						of critical issues with His Majesty, including the need 
						to strengthen our political and economic ties. As you 
						would be aware, South Africa and the Sultanate maintain 
						a very strong bilateral relationship and have done so 
						since diplomatic relations were first established in 
						1994. I hope my visit will lead to further high-level 
						exchanges between the two countries, the importance of 
						which should not be underestimated,” HE Zuma said.<br>
						<br>
						HE Zuma was accompanied by Defence and Military Veterans 
						minister Lindiwe Sisulu, Tourism minister Marthinus van 
						Schalkwyk, Trade and Industry minister Rob Davies and 
						International Relations and Cooperation minister Maite 
						Nkoana-Mashabane.<br>
						<br>
						</font><font face="Verdana" size="2" color="#000080"><b>
						Food hub</b></font><font face="Verdana" size="1"><br>
						<br>
						“One of the important developments has been an agreement 
						signed with the Johannesburg Fresh Produce Market and 
						Sohar Port to take South African produce via Oman to the 
						GCC countries. For this a cold storage facility will be 
						built in Freezone Sohar,” HE Saloojee informs.<br>
						The Freezone Sohar signed an agreement with a consortia 
						comprising leading South African and Omani companies to 
						set up a Food Hub in the Logistics Park of the Zone. The 
						project cost for Phase 1 is approximately RO7mn and the 
						facility is expected to be operational by the first 
						quarter of 2013. The consortia also intends to embark on 
						agricultural enhancement programmes to uplift 
						agricultural and processing practices in Batinah region 
						to world-class standards.<br>
						<br>
						A double taxation avoidance agreement has also been 
						signed between the two countries in a bid to encourage 
						more trade activity between the two countries.<br>
						<br>
						</font><font face="Verdana" size="2" color="#000080"><b>
						Omani eyes on South Africa</b></font><font face="Verdana" size="1"><br>
						<br>
						HE Saloojee wants the bilateral relations to extend 
						beyond trade and economy. He says that Oman is fortunate 
						to have a ruler like His Majesty Sultan Qaboos bin Said 
						who is not only keenly ensuring that the Sultanate is 
						developing on the economic front but also on the 
						cultural side as well. The Royal Opera House is an 
						iconic symbol of Oman’s cultural heritage, HE Saloojee 
						opines.<br>
						In that respect, HE Saloojee has a few plans up his 
						sleeve to enhance the cultural ties between the two 
						countries. “I am looking for an opportunity to bring one 
						of our cultural groups to perform at the Royal Opera 
						House as well as organise a South African Film Festival. 
						Besides those, I would also like to introduce South 
						African handicrafts to Oman,” he says.<br>
						<br>
						</font><font face="Verdana" size="2" color="#000080"><b>
						Veteran diplomat</b></font><font face="Verdana" size="1"><br>
						<br>
						HE Saloojee, while tracing his roots to India from his 
						great grandfather’s side to the state of Gujarat, says 
						that he joined the anti-apartheid movement in South 
						Africa at a very young age. And he has been a diplomat 
						with the current government since 1998. Prior to that he 
						was in the Liberation Movement and was an African 
						National Congress diplomat in Canada for almost 15 
						years. In those days, while the apartheid regime had 
						representatives only in 14 countries, the Liberation 
						Movement had its diplomats in over 50 countries.<br>
						<br>
						“I remained in Canada till the fall of the apartheid 
						system and returned to SA in 1991 after 22 years in 
						exile and I kissed the ground at the first opportunity I 
						got,” recalls HE Saloojee. He worked in the offices of 
						the ANC in the international department and was then 
						posted to Abu Dhabi. After that he headed the ME desk 
						and was again posted to Iran and then to Oman.<br>
