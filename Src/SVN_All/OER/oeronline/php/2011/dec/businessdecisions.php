				<b>Royal Opera House opens<br>
				</b><br>
				His Majesty Sultan Qaboos bin Said presided over the official 
				opening of the Royal Opera House Muscat in October. The iconic 
				opera house symbolises His Majesty’s personal interest in 
				developing the arts, heritage and culture of Oman.<br>
				<br>
				His Majesty the Sultan, on the occasion of the opening of the 
				Opera House, said, “Oman throughout its long history has played 
				prominent roles in various cultural fields and the time has come 
				now to cap that rich march by means of adopting concepts of 
				international culture and by effective contribution to its 
				promotion. Towards that end, we have established the Royal Opera 
				House Muscat as a centre of cultural radiation for the Omani 
				people and all humanity.<br>
				<br>
				The Royal Opera House will play a significant role in 
				disseminating world heritage and reinforcing the principles of 
				peace, co-existence and understanding among all nations and 
				peoples through art events that express a common human cultural 
				heritage of strong meaning and deep impact.<br>
				<br>
				Giacomo Puccini’s Turandot (tale of a frigid Chinese princess 
				named Turandot) was the first opera at the newly inaugurated ROH. 
				This opera was directed by Franco Zeffirelli, conducted by 
				Placido Domingo and performed by the Fondazione Arena di Verona 
				from Italy.<br>
				<br>
				<br>
				<b>Revision of Budgetary numbers <br>
				</b><br>
				The government in April revised its General Budget for 2011 by 
				allocating RO1bn in additional funding towards employment 
				generation of almost 50,000 jobs, unemployment payouts, cost of 
				living allowances, and enhanced pension and social security 
				payouts, among other welfare benefits decreed by His Majesty 
				Sultan Qaboos bin Said.<br>
				<br>
				Around RO444mn has been earmarked to finance the recruitment of 
				40,714 Omani nationals in civil ministries as well as various 
				defence, security and related establishments. A further RO90mn 
				will go towards paying RO150 in monthly unemployment benefits to 
				an estimated 50,000 Omani job-seekers.<br>
				<br>
				The expanded budget also announces an outlay of RO43mn towards 
				meeting the 100 per cent in monthly pensions of social security 
				households. Higher monthly pensions for all retired civilians 
				and military personnel add a further RO89mn to government 
				expenditure. The cost of living allowance now payable to all 
				employees in military and civil establishments accounts for a 
				significant chunk of the additional funding allocated by the 
				government in fiscal 2011, said Minister Responsible for 
				Financial Affairs, HE Darwish bin Ismaeel al Balushi. At 
				RO251mn, it makes up roughly a fourth of the RO1bn in new 
				spending this year.<br>
				<br>
				Additionally, a sum of RO10mn has been earmarked towards 
				increasing the allowances of students enrolled in government 
				colleges, institutes and centres affiliated to the ministries of 
				Higher Education and Manpower.<br>
				<br>
				School bus operators providing transportation services to 
				students will also receive RO15mn in additional funding. Omanis 
				in the private sector stand to benefit from these allocations as 
				well. The revised budget earmarks RO25mn in meeting the cost of 
				paying increased pensions to nationals employed by the private 
				sector. Finally, grants totalling RO14mn have been set aside in 
				support of sporting and youth activities, as well as the 
				information sector.<br>
				<br>
				Al Balushi stressed that the RO1bn in additional funding will 
				largely come from oil revenues earned above the $58 per barrel 
				benchmark set by the government in formulating the 2011 State 
				Budget. As a result of the new financial commitments by the 
				government, public expenditure will burgeon to about RO9.1bn 
				during fiscal 2011, up from RO8.1bn announced in the State 
				Budget unveiled at the start of the year. Consequently, the 
				budget deficit is set to rise to RO1.9bn.<br>
				<br>
				<b>Muscat named Arab Tourism Capital 2012<br>
				</b><br>
				The city of Muscat has been named as the Arab Tourism Capital 
				for 2012 by the Arab Tourism Ministers’ Council.Tourism Minister 
				HE Abdulmalik bin Abdullah Al Khalili said it was proof of the 
				status of this beautiful city on the Arab tourism map. He added 
				the sultanate would strive to make 2012 a ‘distinguished year’ 
				with a number of activities that highlight the vast tourism 
				potential of the country. HE Abdulmalik underlined the 
				importance of Oman’s rich natural environment and its ancient 
				civilisation and cultural heritage, in addition to several 
				tourism resorts that distinguish the Sultanate from the rest of 
				the countries of the region.<br>
				<br>
