				<b>Greater powers for Majlis A’Shura</b></font><p align="center" style="margin: 3px 10px">
				<font face="Verdana" size="1">
				<b><br>
				</b>
				<img border="0" src="images/bbr_dec3.jpg" width="450" height="247" style="border: 1px solid #808080" class="style4" hspace="6" vspace="3"></font><p align="justify" style="margin: 3px 10px">
				<font face="Verdana" size="1">
				<br>
				Perhaps the most defining moment in the political history of 
				Oman has been the empowerment of Majlis A’Shura with more 
				legislative and auditing powers in October 2011. His Majesty 
				Sultan Qaboos bin Said had promised more powers to the Shura 
				Council in the aftermath of the pro-reform protests in 
				February-March.<br>
				<br>
				A Royal Decree widened the powers of Majlis Oman and some 
				provisions of the Basic Law of the State allowing members of the 
				Majlis to take part in proposing laws and also suggesting 
				changes in government regulations. The 84-member Majlis A’Shura 
				which was elected in October was allowed to select its chairman 
				and speaker by a vote. The Council will also be able to question 
				the ministers. The chairman of Majlis was also made a part of a 
				core team that will be involved in choosing the successor to His 
				Majesty should there be any dispute in the Royal Family Council.<br>
				<br>
				<b>INDUSTRIAL DEVELOPMENTS</b></font><p align="justify" style="margin: 3px 10px">
