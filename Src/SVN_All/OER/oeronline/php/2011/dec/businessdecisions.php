				Vale Oman sets new benchmarks for steel<br>
				Oman entered a new stage in the steel industry as Vale 
				successfully began operations in its Industrial Complex at Sohar 
				Industrial Port in June. The company’s Pelletising Plant and 
				Distribution Centre aims primarily to transform iron ore into 
				pellets to cater to the growing demands of iron ore in the 
				Middle East, North Africa and India (MENAI). Vale Oman 
				Pelletising Company’s production capacity is designed to reach 
				18 million tones. Vale’s Industrial Complex in Sohar comprises a 
				two-unit pelletising plant, each with a nominal production 
				capacity of 4.5 million metric tonnes of direct reduction 
				pellets per year, in addition to a distribution centre with an 
				annual capacity of 40 million metric tonnes.<br>
				<br>
				<b>Voltamp opens facility</b><br>
&nbsp;</font><p align="center" style="margin: 3px 10px">
				<font face="Verdana" size="1">
				<img border="0" src="images/bbr_dec5.jpg" width="381" height="199" style="border: 1px solid #808080" class="style4" hspace="6" vspace="3"></font><p align="justify" style="margin: 3px 10px">
				<font face="Verdana" size="1">
				<br>
				Voltamp’s new Sohar factory was opened for production of 
				transformers in the second half of 2011. Its initial orders have 
				been placed by Oman Electricity Transmission Company (OETC) for 
				125MVA 132/33kV class transformers. The company’s new RO11mn 
				plant was inaugurated in the Sohar Industrial Estate by HH 
				Sayyid Shihab bin Tariq al Said in the presence of HE Sa’ad al 
				Saa’di, minister of Commerce and Industry, and W Y Lin, 
				president of Taiwanese technology partner Tatung. The plant has 
				the capability to produce and repair 315MVA 220kV class 
				transformers - the biggest in the region.<br>
				<br>
				<br>
				<b>Oman Drydock Company’s soft launch</b><br>
				<br>
				The Al Wusta Region of the Sultanate in May witnessed the launch 
				of the soft operations of the Oman Drydock Company in Duqm when 
				it received the first two vessels owned by a Belgian company for 
				maintenance. This augurs well in laying the strong pillars for a 
				new industrial development in the Sultanate namely the marine 
				industry.<br>
				<br>
				HE Said bin Hamdoon al Harthy, undersecretary of the Transport 
				and Communications, for Ports and Marine Affairs and chairman of 
				ODC said, “As of today, the government pumped more than RO700mn 
				of investments in the construction of the dry dock.” The drydock 
				will be ready by the end of August 2012 to receive all types of 
				commercial vessels of different sizes including oil tankers, 
				container vessel of the fourth and fifth generations and other 
				vessels with a capacity of 600,000 tonnes. <br>
				<br>
				<b>SMN Power launches IPO</b><br>
				<br>
				SMN Power Holding launched its initial public offering (IPO) of 
				6,987,246 shares representing 35 per cent of the total share 
				capital in September. International Power is a founding 
				shareholder of SMN Power Holding, together with Mubadala 
				Development Company of Abu Dhabi and National Trading Company of 
				Oman. The only IPO in Oman in 2011, and one of the very few in 
				the region this year, the SMN Power Holding IPO attracted strong 
				interest from regional and international investors. The issue 
				was oversubscribed 1.7 times, raising RO40.9mn against the 
				target size of RO24.6mn. Following the IPO, 35 per cent of the 
				shares of SMN Power Holding will be held by public investors, 
				with the founder shareholders retaining the balance of 65 per 
				cent of the shares.<br>
				<br>
				<br>
				<b>Jindal Shadeed Iron and Steel plant<br>
&nbsp;</b></font><p align="center" style="margin: 3px 10px">
				<font face="Verdana" size="1">
				<img border="0" src="images/bbr_dec6.jpg" width="383" height="232" style="border: 1px solid #808080" class="style4" hspace="6" vspace="3"></font><p align="justify" style="margin: 3px 10px">
				<font face="Verdana" size="1">
				<br>
				The integrated steel complex of Jindal Shadeed Iron and Steel 
				was dedicated to the nation at the Port of Sohar. The RO200mn 
				project will produce 2 MTPA of finished steel by the second 
				phase of its operations.<br>
				<br>
				The JSPL took over Shadeed Iron and Steel in July, 2010 and 
				commissioned its Direct Reduction Unit with a capacity of 1.5 
				million tonnes per annum (MTPA) for Hot Briquetted Iron (HBI) 
				and Hot DRI (HDRI) four months ahead of schedule on December 5, 
				2010 and commercial production started from January 1, 2011.<br>
				<br>
				<b>Amendments to Labour Law</b></font><p align="justify" style="margin: 3px 10px">
				&nbsp;<p align="center" style="margin: 3px 10px">
				<font face="Verdana" size="1">
				<img border="0" src="images/bbr_dec7.jpg" width="175" height="189" style="border: 1px solid #808080" class="style4" align="right" hspace="6" vspace="3"></font><p align="justify" style="margin: 3px 10px">
				<font face="Verdana" size="1">
				In October, the Ministry of Manpower effected significant 
				amendments to Oman’s labour law. As per the amendments, the 
				workers are entitled to two-day weekly off and not more than 45 
				hours of work per week. Any working hours above this should be 
				treated as overtime.<br>
				<br>
				The amendments also stipulates that salaries of employees should 
				be deposited in banks within 7 days of completion of a month. 
				The amendments also include the workers’ entitlement to a paid 
				annual leave of not less than 30 days in addition to a paid 
				emergency leave for six days during the year. HE Abdullah bin 
				Nasser al Bakri, Minister of Manpower, confirmed that the recent 
				amendments to the provisions of the Labour Law took into account 
				the interests of all parties involved in the process of 
				production, with a special focus on procedures that ensure a 
				healthy relationship between employers and employees.<br>
				<br>
				<b>Approval for Islamic Banking</b><br>
				<br>
				The Central Bank of Oman (CBO), in a landmark decision allowed 
				conventional banks operating in the Sultanate to offer Sharia-based 
				financial services – basically Islamic banking. HE Hamoud bin 
				Sangour al Zadjali, executive president of CBO stressed that the 
				imminent introduction of Islamic Banking services would help 
				support the country’s economic development. Oman offers a large 
				potential for the growth of Islamic banking in the future and it 
				is expected to to complement the conventional banking sector in 
				promoting the development of the economy. The CBO’s board of 
				governors had approved in May the inclusion of Islamic banking 
				in the provisions of the Omani Banking Law, in line with the 
				Royal Directives of His Majesty the Sultan. The apex bank also 
				approved the establishment of the country’s first Islamic bank – 
				Bank Nizwa – to provide banking services under the Sharia Law, 
				while another new bank, Al Izz International Bank, has been 
				authorised to offer Islamic banking services as well.<br>
				<br>
				<b>Tourist port</b><br>
				<br>
				Port Sultan Qaboos located in the capital area of Muscat and one 
				of the three big ports in the country, will be converted from a 
				commercial port to a tourist port. All commercial, import, 
				export, general cargo and container activities will be 
				transferred to the Sohar Industrial Port.<br>
				<br>
				The commercial and tourist ports have major strategic importance 
				in sustainable development. The development of the port sector 
				will also enhance the ‘Oman Vision 2020’, which aims at 
				diversifying the resources of the national economy and reducing 
				reliance on oil revenues. Cruise shipping is expected to be the 
				mainstay of the converted port. In July, the Ministry of 
				Transport and Communications and the Ministry of Tourism issued 
				a joint statement that in coordination with the respective 
				public and private departments, the ministries will develop a 
				master plan and detailed designs for the port and its associated 
				services and utilities.<br>
