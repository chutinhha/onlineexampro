				<b>Deep water jetty</b></font><p align="center" style="margin: 3px 10px">
				<font face="Verdana" size="1">
				<b><br>
				</b>
				<img border="0" src="images/bbr_dec2.jpg" width="450" height="233" style="border: 1px solid #808080" class="style4" hspace="6" vspace="3"></font><p align="justify" style="margin: 3px 10px">
				<font face="Verdana" size="1">
				<br>
				The Port of Sohar’s new deepwater jetty was built at a cost 
				RO100mn to accommodate world’s largest bulk carriers at this 
				industrial port. Sohar Industrial Port Company (SIPC), the 
				landlord-operator of the industrial port constructed this 
				1,380-metre-long deepwater jetty, boasting drafts of 25 metres. 
				The first ship to call at the port was MV Bulk Asia, a 170,000 
				deadweight-tonne (DWT) Cape-size vessel carrying iron ore for 
				Jindal Shadeed Iron and Steel company. The deepwater jetty has a 
				capacity to handle the new generation of bulk carriers with 
				capacities equalling three times that of MV Bulk Asia. The 
				state-owned Oman Shipping Company (OSC) is currently building 
				four Ultra Large Ore Carriers (ULOCs), each of a staggering 
				400,000 tonnes capacity. <br>
