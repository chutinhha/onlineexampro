						Winehouse</b></i></font> (1983 –2011), who was found 
						dead at the age of 27, was one of the outstanding 
						singers of her generation, known for her powerful deep 
						contralto vocals and her eclectic mix of musical genres 
						including R&amp;B, soul and jazz. Winehouse’s music spoke to 
						people so persuasively that her second album, Back to 
						Black, became Britain’s bestselling record of 2007 and 
						reached No 2 in the US, making her one of only a few 
						British female soloists to achieve that level of 
						transatlantic recognition. Its success spurred sales of 
						her initially overlooked first album, Frank (2003), so 
						titled because of the diary-style lyrics that produced 
						songs such as Stronger Than Me, which railed against a 
						“ladyboy” ex-boyfriend. On 14 February 2007, she won a 
