						century. A former movie billboard artist who rose to 
						become India’s most sought-after painter before going 
						into self-imposed exile during an uproar over nude 
						images of Hindu icons, Husain has been widely regarded 
						as the “Picasso of India” and has influenced a whole 
						generation of artists in the country. He mastered not 
						only the techniques of line and colour, but also those 
						of becoming a superstar of modern Indian art. Husain was 
						associated with Indian modernism in the 1940s. A 
						dashing, eccentric figure who dressed in impeccably 
						tailored suits, he went barefoot and brandished an 
						extra-long paintbrush as a slim cane. He never 
						maintained a studio but spread his canvases out on the 
						floor of whatever hotel room he happened to be staying 
						in and paying for damages when he checked out. His 
						narrative paintings, executed in a modified Cubist 
						style, can be caustic and funny as well as serious and 
						sombre. His themes—usually treated in series—include 
