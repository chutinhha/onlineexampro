						commitment to work was so remarkable that she continued 
						to discharge her ministerial duties even from her 
						hospital room till she died. She was also the 
						chairperson of Omran, the government organisation to 
						promote tourism and tourism integrated properties in 
						Oman. It was her foresight that led Oman to host the 4th 
						International Conference on Responsible Tourism 
