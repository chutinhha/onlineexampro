						<p align="justify" style="margin: 3px 10px"><strong>
						<span class="style27">In uncharted waters</span></strong><p class="style15">
				<strong><span class="style14">Gulf School of Business (GSB) 
				seeks to revolutionise management education by creating an 
				appetite for continuous learning, says Prof S Sundararajan, 
				Director, GSB and Morison Muscat Knowledge Centre</span></strong><br>
						<p align="justify" class="style13">
				<strong>Can you give us a brief on Gulf School of Business and 
				its vision?<br>
				</strong><br>
				GSB seeks to dynamically embed the spirit of intrapreneurship/entrepreneurship 
				in the participants with a view to create enduring value to the 
				three constituents-the participant, the organisation, and the 
				gulf economy. The programme gently nudges and steers the 
				participant to break away from the comfort zone of functional 
				silos and accept the more challenging role of developing a 
				holistic approach to management. We achieve this vision by 
				adopting various innovative processes. The programmes are open 
				only to managers; the faculty are exposed to business issues 
				apart from being conceptually strong in the chosen field of 
				specialisation; and we adopt the rule of 60/40 ie. 60 per cent 
				of class time for faculty and 40 per cent for the participants. 
				Cases are dynamically created in the class room based on the 
				discussion of issues faced by the participants in their real 
				life. Sessions are designed to achieve movement from ‘practice 
				to theory’-deriving the theory based on practices. Besides, we 
				also continuously focus on local setting and global context. The 
				inputs are designed to sensitise the participants to achieve &#8232;a 
				fine balance between profit, people &#8232;and planet.<br>
				<br>
				<strong>GSB was launched in 2009. How has been the response so 
				far and how do you distinguish yourself from other management 
				institutes in the region?<br>
				</strong><br>
				We are pleasantly surprised to receive an encouraging response 
				from educated young managers who are willing to do the programme 
				by paying from their pockets. Our USP is the emphasis on 
				fostering the spirit of entrepreneurship in the participants.<br>
				<strong><br>
				What are the long and short duration programmes GSB offers?<br>
				</strong><br>
				We are presently offering four long duration dual certification 
				management programmes in collaboration with U21 Global. They are 
				one-year part time ‘competency in practising management/ post 
				graduate diploma of business administration’, eight-month part 
				time ‘proficiency in practising management/ post graduate 
				certificate of management studies’, eight-month part time 
				‘proficiency in financial management/ post graduate certificate 
				of financial management’ and eight-month part time ‘proficiency 
				in human resource management/ post graduate certificate of human 
				resource management’. We also have several short duration 
				programmes on topics of current relevance.<br>
				<strong><br>
				How is the scope of management studies in Oman?<br>
				</strong><br>
				If the ambition to excel and strike on their own exhibited by 
				the participants, especially the ladies, is any indication of 
				the future, the region is poised for a great leap into high 
				growth, technology driven entrepreneurship and this trend is 
				good news for the providers of management education.<br>
				<strong><br>
				You recently tied up with U21 global to provide high impact 
				programme in practicing management. What are your expectations?<br>
				<br>
				</strong>We expect to excite the participants by making them to 
				straddle between physical and virtual class rooms! </td>
