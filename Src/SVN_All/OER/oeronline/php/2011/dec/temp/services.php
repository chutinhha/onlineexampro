						<p align="justify" style="margin: 3px 10px">
						<span class="style27"><strong>INOVA, BGP hold Vibroseis 
						3D seismic survey</strong></span><p class="style15">
				<strong><span class="style14">The project seeks to bridge the 
				gap between productivity and image quality with innovative 
				engineering and operational excellence</span></strong><p class="style15">
				INOVA Geophysical Equipment Limited and the Bureau of 
				Geophysical Prospecting (BGP) announced the completion of a high 
				productivity Vibroseis 3D seismic survey in Oman using INOVA’s 
				ARIES II recording system, Vib Pro source controllers, and 
				AHV-IV Vibroseis vehicles. The purpose of the field test was to 
				acquire data to validate the capabilities of INOVA recording 
				technology in acquiring slip-sweep, Distance Separated 
				Simultaneous Sweep (DS3), Distance Separated Simultaneous 
				Slip-Sweep (DS4) &#8232;and Independent Simultaneous Sweep &#8232;(ISS) 
				data. <br>
				<br>
				During the test, the Aries II – Vib Pro recording – shooting 
				system was able to successfully record both DS3 and DS4 data 
				with dynamic fleeting. This was especially important in the 
				rugged terrain in which the test was completed, as vibrator 
				move-up times varied significantly from vehicle to vehicle. For 
				this project, record production numbers for DS3 with Slip-Sweep 
				(DS4) reached over 830 vibration points per hour. <br>
				<br>
				As advancements in techniques and equipment continue to emerge, 
				equipment manufacturers, geophysical service operators and E&amp;P 
				companies have a vested interest in a continuous improvement in 
				Vibroseis operations. The geophysical objectives of increased 
				surface sampling with high-channel count acquisition systems can 
				be met with high productivity cost-effective Vibroseis 
				techniques such as those used in these tests.<br>
				<br>
				“The result of this project is an illustration of the 
				collaboration between BGP and INOVA in bridging the gap between 
				productivity and image quality with innovative engineering and 
				operational excellence. This milestone is also indicative of our 
				growing competitive advantage in delivering increased 
				operational efficiencies to our clients, combined with superior 
				customer support,” says Glenn Hauer, senior vice president of 
				product management for INOVA Geophysical.<br>
				<br>
				INOVA’s ARIES II land acquisition system, Vib Pro source 
				controllers and the AHV-IV vibrator line are designed to 
				maximise return on investment by reducing equipment related 
				downtime and minimising the impact of harsh environmental and 
				operational conditions. They support high channel count, 
				advanced Vibroseis acquisition for all 2D, 3D, or time-lapse 4D 
				seismic programmes.<br>
				<br>
				INOVA Geophysical Equipment Limited is a leading provider of 
				land geophysical technology, including source and source control 
				systems, cabled and cableless land acquisition systems, and 
				advanced seismic sensing devices. Managed as an independent 
				company, INOVA is a joint venture owned 51 per cent by BGP (a 
				wholly owned subsidiary of China National Petroleum Corporation) 
				and 49 per cent by ION Geophysical Corporation (NYSE: IO).<br>
				<br>
				BGP is one of the world’s leading geophysical service companies, 
				delivering a wide range of technologies, services and equipment 
				to the oil and gas industry world-wide. The company is engaged 
				in seismic data acquisition, processing, interpretation, GME and 
				multi-client surveys, equipment manufacturing and software R&amp;D.</td>
