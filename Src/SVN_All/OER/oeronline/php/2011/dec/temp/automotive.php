	<tr>
						<td valign="top">
						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">Where 
						luxury meets performance</font></b><p align="justify" class="style3">
				<font face="Verdana" size="1">
				<img border="0" src="images/feb-automotive1.jpg" style="border: 1px solid #808080; float: left;" class="style4"></font><strong><span class="style5">Stefan 
				Mecha, Manging Director, Volkswagen, Middle East speaks to 
				Mayank Singh about the new Touareg in an exclusive interview. 
				Excerpts:</span></strong><br>
				<br>
				<strong>The new Touareg is being marketed as a technically innovative 
				vehicle that is longer, lighter, roomier, sleeker and safer than 
				its predecessor. What is the USP of this product?<br>
				</strong><br>
				The Touareg has proven its record over the last seven years. 
				It’s a well known car in the region. It’s luxurious and has a 
				lot of good features. The Touareg did very well in its first 
				life cycle. As oil prices breached the $100 per barrel mark, 
				there has been a lot of attention on fuel economy of SUVs. 
				Keeping this in mind we have made a number of improvements in 
				the car. Though the new Touareg is 41mm longer, we have reduced 
				the weight of the car by 200kgs – which is almost 10 per cent of 
				the total weight. This is extremely important because when you 
				drive the car as it is more handy and dynamic. The vehicle was 
				always positioned as a luxurious vehicle so we have enhanced 
				those features. We have a new GPS and navigation system, new 
				leather, the largest ever panoramic sunroof ever used in an SUV. 
				Improvements have also been made in the vehicle technologically. 
				All this is important to keep the momentum of the Touareg going.<br>
				<br>
				<strong>Safety of the vehicle seems to be an overriding theme in 
				the new Touareg. Do you see this as a major advancement over the 
				previous version?<br>
				</strong><br>
				We want to minimise accidents and the Touareg provides the 
				driver assistance in reducing such incidences, either directly 
				or indirectly. Sometimes when you change lanes you tend to 
				forget to look over your shoulders. There is an electronic 
				warning system that alerts one on whether it is safe to do so. 
				An adaptive cruise control (ACC) autonomously controls the safe 
				distance from vehicles ahead in traffic.<br>
				<br>
				<strong>The new Touareg seems to have something for everyone. Is 
				there any specific target group that you are looking at?<br>
				</strong><br>
				Our target audience remains the same – business people who want 
				to drive a big and spacious car, families who want to take a 
				weekend break, a sporty car for young executives and a vehicle 
				that can be used on a daily basis. It has a very broad audience 
				that it appeals to. It’s a car which has great off road 
				capabilities though it is mostly driven in the city.<br>
				<br>
				<strong>The Touareg competes with the Porsche Cayenne at the top 
				end and the Toyota Land Cruiser at the entry level. Has the 
				vehicle been deliberately positioned in such a way so that it 
				can attract customers from both ends of the spectrum?<br>
				</strong><br>
				This is the opportunity for us, if you look at the car it 
				competes in the luxury SUV segment against the competitors that 
				you just mentioned. If you look at the price value equation of 
				the car it is very good. As they say Volkswagen is a people’s 
				car and we do not want to be a super expensive SUV. 
				Accessibility is the key. Though the Touareg comes with good 
				features and performance it is priced reasonably. We are able to 
				wean customers from both ends, but you need to realise that 
				someone who buys a very robust SUV uses it in a very different 
				way than one would use a Touareg. We are confident that we have 
				a good share of the premium SUV segment and can attract more 
				customers from the robust SUV segment.<br>
				<br>
				<strong>You have introduced a hybrid version of the car. Was 
				green a big theme for the new Touareg?<br>
				</strong><br>
				Definitely, the whole engineering is geared towards reducing 
				carbon footprint and fuel consumption, making the car lighter 
				and downsizing the engine to get more power. While we realise 
				that the hybrid car may not be a big draw in the GCC region, it 
				is more suited to Europe, US and Asia.<br>
				<br>
				But for us hybrid is only one technology amongst a number of 
				available choices, we believe that combustion engines will be 
				the pre dominant technology of the future, so we are committed 
				to improving our combustion engine technology. We will also go 
				for electric cars. The effort is to use a mix of various engine 
				technologies.<br>
				<br>
				<strong>One charge against the new Touareg is that the engines 
				remain the same. How would you react to this?<br>
				</strong><br>
				If you look at the life cycle of the Touareg, we changed the 
				engine in the last version. In the new Touareg there is an 
				evolution but we have not changed the engine completely because 
				it is such a well performing engine. What was important for us 
				was the interaction of the engine with the gear box and since we 
				have changed the gear box the drive of the vehicle has improved 
				dramatically. We will shortly bring the 4.2 litre, 8-cylinder 
				engine to the region fulfilling the aspirations of the people 
				looking for more power and high torque.<br>
				<br>
				<strong>Has the pricing of the vehicle changed due to the 
				innovations that have been introduced?<br>
				</strong><br>
				We are striving to maintain the same prices as in the past, but 
				the final price also depends on our partners and so it varies 
				across countries as per the specifications. In Oman, it has a 
				starting price of RO16,000 and then there is the top end which 
				comes at RO22,000, with big navigation system and big rims.<br>
				<font size="1"><br>
				</font><strong>When is V8 engine expected to be launched here?<br>
				<br>
				</strong>We will be introducing the V8 in the second quarter of 
				2011.<br>
				<br>
				<strong>What are your sales expectations from the Touareg in the 
				Middle East?<br>
				<br>
				</strong>During the boom years of 2006-08 we sold 3,400 Touaregs 
				a year in the region, due to the strong pull factor. We are 
				confident that this car can bring us those numbers and so we 
				have a lot of expectations from this car.<br>
				<br>
				<strong>What are your thoughts on the Oman market?<br>
				</strong><br>
				Oman can do around 400 cars a year which come to roughly 30 plus 
				cars a months, but this will not come overnight. Our 
				organisation has a lot of potential but we need to establish the 
				business and increase our exposure. We have many branches across 
				the country but these need to be used efficiently.<br>
				<font size="1"><br>
				<strong>The Touareg has had some reliability concerns in the 
				past, have those been addressed?<br>
				<br>
				</strong>The reliability issue dates back to five years and even 
				in the existing model we have had no such issues. The problem is 
				that once you have had a problem it takes ages to get rid of it. 
				We have considerably improved the perceived quality of our cars. 
				There were certain issues that customers raised and we have had 
				a closer look at these and in the last three to five years. We 
				have not had to face the kind of issues (automobile recalls) 
				that other car makers have had lately, but we are not pointing 
				fingers at anyone as recall is something that can happen to any 
				brand. Engineering a car is a complex issue as it requires so 
				many components from various suppliers and so we do not pretend 
				that this cannot happen to us. What I can claim confidently is 
				that people in our Volkswagen headquarters work very hard on 
				getting the greatest quality out of these cars. </font></td>
