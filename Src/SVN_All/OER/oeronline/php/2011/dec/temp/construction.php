				been revolutionising the way investment transactions are 
				conducted in the Sultanate. Iskan Oman was established by a 
				diversified base of financially strong and well-reputed 
				shareholders including prominent financial institutions, 
				government pension funds, high net worth individuals and 
				companies from the Sultanate and Kuwait. Two major shareholders 
				of the company are Iskan Housing Finance Company KSC and The 
				Public Institution for Social Security in Kuwait. Modeled on its 
				parent company in Kuwait (Iskan Finance Company and the Public 
				Institution for Social Security), Oman was chosen as the site 
				for the first regional expansion, after exhaustive research, 
				planning and analysis.<br>
				<br>
				“We looked at the market in Oman,” says Adil Mohammed Al Bader, 
				chairman of Iskan Oman, “and determined that it would provide an 
				ideal location for us to grow and nourish a regional expansion.”<br>
				<br>
				“Oman provided a unique opportunity for us in terms of 
				stability; while other markets were imploding in a spectacularly 
				catastrophic fashion as a result of the global financial crisis, 
				Oman remained comparatively stable. This was thanks to 
				sustainable and conservative economic government expenditure,” 
				says Al Bader.<br>
				<br>
				“Since our inauguration over two years ago, Iskan Oman has grown 
				from strength-to-strength,” says Dr Rashid Al Balushi, managing 
				director, Iskan Oman. Of particular significance to the planned 
				activities that Iskan Oman has scheduled for 2011, is the 
				opening of the multimillion rial development, Zahrat Al Khareef, 
				Dhofar. The gated community project, offering freehold ownership 
				for Omani and GCC citizens, is the first of its kind and one of 
				the largest property developments to date in Salalah.<br>
				<br>
				The first phase of the prestige-living development is slated for 
				completion in the final quarter of this year. Zahrat Al Khareef, 
				Dhofar has been designed to harmoniously blend with the 
				environment, complementing the surroundings that attract 
				thousands of visitors each year to witness the subtropical 
				lushness in the Khareef Season. <br>
				<br>
				“It is our commitment to actively contribute to our economy as 
				well as our societies; Iskan Oman seeks new and untapped avenues 
				and translates them into viable opportunities generating lasting 
				growth,” says Al Bader. Located in the Al-Wadi area in Salalah, 
				the Zahrat Al Khareef, Dhofar project covers an area of 
				approximately 29,000 square metres. Ten-storey buildings with a 
				gross built up area of 130,000 square meters will offer 586 
				living units in a mix of one, two and three bedroom and duplex 
				apartments.<br>
				<br>
				In addition, a family-friendly club house equipped with a modern 
				gymnasium and other recreational facilities will serve as a 
				fitness and community centre for residents, bringing together 
				the occupants to socialise and relax in world class luxury. 
				Additional facilities such as a swimming pool with 
				professionally designed landscaping further complement the 
				recreational offerings of Zahrat Al Khareef, Dhofar development.<br>
				<br>
