				<img border="0" src="images/banking_dec1.jpg" width="150" height="191" style="border: 1px solid #808080" align="right" hspace="8"></font><font face="Verdana" style="font-size: 9pt; font-weight: 700">Sulaiman 
				Al Harthy, Deputy General Manager, Consumer Banking Group, 
				BankMuscat shares his thoughts on Islamic Banking with Mayank 
				Singh</font><p align="justify" style="margin: 10px">
				<font face="Verdana" size="1"><b>The Central Bank of Oman (CBO) 
				has allowed Islamic Finance/Islamic Banking in Oman. What impact 
				will the decision have on the banking and finance industry in 
				Oman?</b><br>
				There are several reports about the probable impact of the 
				decision on the banking and finance industry in Oman. Some of 
				these reports suggest that Islamic Banking has taken about 15 
				per cent of the conventional business in the region. As we are 
				in a country where a majority of people follow Islam, one can 
				assume that hundreds of thousands of Omanis will definitely 
				prefer Islamic Banking compared to conventional banking. So, 
				there will be some impact, however the extent of this impact is 
				difficult to estimate as of now.<br>
				<br>
				<b>Some analysts and experts have claimed that Islamic Finance 
				in Oman is a business opportunity running into billions of 
				dollars. Do you agree with such claims?</b> <br>
				Estimates or claims about Islamic Banking being worth billions 
				of dollars in Oman, may be on the higher side, but yes, it is 
				definitely a big opportunity.<br>
				<br>
				<b>Islamic Banking has been growing rapidly across the globe, 
				what makes it an attractive proposition for customers?</b><br>
				Islamic Banking is an ethical proposition, and to many who want 
				to have financial arrangements that are close to their faith, it 
				is a very good alternative.<br>
				<br>
				<b>How is BankMuscat gearing up to take up the opportunity and 
				challenge presented by Islamic Banking? </b><br>
				We are eagerly waiting for CBOs approval, and would immediately 
				increase our speed to launch our window.<br>
				<br>
				<b>The Central Bank of Oman has allowed for both Islamic Banks 
				and Islamic Banking windows. What kind of a facility is 
				BankMuscat looking at for its customers? </b><br>
				Since the CBO has allowed for Islamic Banking related window 
				structure, we will be opting for a window. The other reason for 
				opting for an Islamic Banking window is that it will be faster 
				and quicker to launch.<br>
				<br>
				<b>There are concerns that existing banks may not segregate the 
				sources of funding and accounts between conventional and Islamic 
				Banking. How will BankMuscat ensure that there is a complete 
				separation between these two businesses?</b><br>
				This is one area about which we are going to be very strict. 
				BankMuscat has detailed policies and procedures in place, we 
				have the system capabilities that will allow us to do so. In 
				addition, we will religiously comply with the regulatory 
				requirements on the issue and go by the guidance of our Sharia 
				Board.<br>
				<br>
				<b>When does BankMuscat expect to start offering Islamic Banking 
				services in Oman? </b><br>
				Once we receive the requisite CBO approvals, we will immediately 
				start offering Islamic Banking. <br>
