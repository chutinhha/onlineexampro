						<p align="justify" style="margin: 3px 10px">
						<font face="Verdana" size="4" color="#C00000"><b>Making 
						money in high yield asset classes!<br>
						<br>
						</b></font><font face="Verdana" style="font-size: 9pt">
						<b>In a world where bank deposits pay only 2 per cent, 
						investors have several high yield opportunities in the 
						GCC/MENA debt market<br>
						</b></font><font face="Verdana" size="1"><br>
						<i><font color="#333333">BY Matein Khalid<br>
						</font></i><br>
						GCC investors are naturally interested in double digit 
						dollar returns without taking currency risk. There are 
						several high yield opportunities in the GCC/MENA debt 
						market that could make serious money for investors. Take 
						the DIFC Investments sukuk that matures in June 2012 and 
						offers a 12 per cent yield if it is redeemed at par at 
						maturity.<br>
						<br>
						Dubai’s economic model necessitates that finance and 
						aviation/ports are treated as strategic industries by 
						the government. This means that DIFC Investments, 
						despite operating losses of $850mn in the past two 
						years, the devaluation of its non-core asset sale 
						pipeline in MENA due to the Arab spring, will redeem its 
						$1.3bn Sukuk placed with global investors at the height 
						of the credit bubble. Jebel Ali and DIFC are surely 
						strategic and therefore merit credit support, even 
						though refinancing risk on the sukuk exists.<br>
						<br>
						The DEWA (Dubai Electricity and Water Authority) ten 
						year debt yields 7 per cent, compared to safe haven GCC 
						sovereign debt where ten year debt in Qatar and Abu 
						Dhabi offer a mere 3.2 per cent yield. As an investor, I 
						believe DEWA is underpriced since its debt is backed by 
						the utilities huge cash flow, with revenues up 40 per 
						cent in 2011 to date to AED6.6bn. It is entirely 
						possible that DEWA (2020 maturity) bonds credit risk 
						will compress relative to the US Treasury yield curve.<br>
						<br>
						</font><font color="#000080" size="2" face="Verdana"><b>
						In post-Baathist Iraq</b></font><font face="Verdana" size="1"><br>
						<br>
						A high risk but high reward strategic trade would be to 
						buy the Republic of Iraq sovereign callable dollar 
						denominated Eurobond that yields 7.5 per cent but only 
						if the price falls below 80 or a potential yield of 8 
						per cent. Iraq’s financial fundamentals have undergone a 
						paradigm shift since 2006, when Baghdad restructured 
						$27bn in Saddam era debt with credit that ranged from 
						South Korea’s Hyundai to Russia’s Lukoil then negotiated 
						an exchange offer (with a 80 per cent haircut) into its 
						inaugural post-Baathist dollar denominated Eurobond.<br>
						<br>
						Iraq now exports 2.4 million barrels of oil a day and 
						the West Qurna/Majnoon bidding round attracted the most 
						powerful consortia in Big Oil. These include Exxon, Oxy, 
						Total, BP and several Chinese oil companies.<br>
						<br>
						Turkish, US, Britain and Norwegian oil companies are 
						active in Kurdistan despite the fact that no Petroleum 
						law is in place. Royal Dutch Shell has committed to a 
						natural gas infrastructure/producing deal in Basra worth 
						$12bn with the Iraqi government. The Iraqi stock market 
						is up 40 per cent, the best performing in MENA. Iraq’s 
						GDP growth next year could top 12 per cent, the highest 
						in the Arab world, comparable only to LNG colossus 
						Qatar. The Central Bank of Iraq was looted by Saddam and 
						his sons in March 2003 even as American tanks entered 
						the streets of Baghdad in Bush’s “shock and awe” 
						invasion.<br>
						<br>
						The Iraqi government is a coalition of Sunni, Shia and 
						Kurdish parties, though Muqtada Al Sadr’s Mehdi Army is 
						a political kingmaker in Parliament, with 40 MPs. This 
						means the Nouri Al Maliki government exits (and could 
						well fall) due to sectarian politics. A far more serious 
						risk to Iraqi stability is terrorism, though Al Qaeda in 
						Mesopotamia has lost its desert sanctuaries in Anbar and 
						Diyala provinces as the local tribes defeated them in 
						the 2007 US “surge”. Another wild card after the exit of 
						US military troops from Iraq in December 2011 is a 
						potential Israeli/US preemptive strike on Iran’s nuclear 
						sites. This means that the Iraqi sovereign bond is both 
						high risk and as the risk is geopolitical, it cannot be 
						quantified.<br>
						<br>
						A far safer asset class, in my opinion, is US mortgage 
						REITs listed on the New York Stock Exchange. These can 
						offer dividends as high as 15-20 per cent (though 
						taxable to non-US investors at a 30 per cent rate) as 
						they are portfolios of agency collateralised mortgage 
						obligations/pass through (CMO’s) and secondary market 
						loans/notes leveraged via borrowing in the money 
						markets. This beautiful high yield return is rock solid 
						as long as the Federal Reserve does not raise the Fed 
						Fund rate by 2012, as Chairman Bernanke has committed 
						after successive FOMC conclaves. This means the funding 
						cost for mortgage REIT’s will not rise though periodic 
						mood swings in the global markets due to Euro/Italian 
						debt mean safe haven inflows into long duration US 
						Treasury debt and therefore a potential risk in hedging 
						cost.<br>
						<br>
						Hedging costs are also correlated to a rise in debt 
						market volatility, which I cannot see falling as the 
						Eurozone debt soap opera reaches its endgame.<br>
						<br>
						However, it is extraordinarily profitable to invest in 
						mortgage REITs now and collect 15 per cent after tax 
						dividend returns because US money centre banks, flush 
						with deposits from cash rich corporate and deleveraging 
						consumers at a time of sluggish loan/lease growth, take 
						advantage of Bernanke’s QE2 and the leveraged US 
						Treasury debt yield curve to put on profitable “carry 
						trade” in high yield mortgage debt even as they managed 
						the repayment risk via dynamic hedging. The 
						macroeconomic case for a housing rebound is not 
						necessary to make money in big board listed mortgage 
						REITs.<br>
						<br>
						I also believe GCC investors are totally under invested 
						in energy master limited partners (MLPs) again listed on 
						the New York Stock Exchange. Shale oil in Texas, the 
						Rocky Mountain States and North Dakota means onshore 
						crude production will double in the next four years to 
						2.5 million barrels a day. This means a money making 
						bonanza for owners of fixed reserve midstream 
						infrastructure assets like pipelines, crude 
						storage/terminals and natural gas gathering/processing 
						plants. The MLPs thus earn revenues from fees/tariffs on 
						the pipeline as well as arbitrage profits from crude 
						price differentials as they optimise their midstream 
						networks.<br>
						<br>
						An energy pipeline operator can easily offer 7 per cent 
						yield and 8 per cent cash distribution growth for a 15 
						per cent dollar return. In a world where banks deposits 
						pay 2 per cent, this is a highly profitable, high yield!<br>
