				highlighting for the last seven years the developments made by 
				the country in various socio-economic fields with a special 
				focus on inclusive growth. This year’s edition has been 
				dedicated to His Majesty’s vision to create a modern country 
				with state-of-the-art technology and infrastructure to take Oman 
				further ahead on the road to prosperity. It focuses on the 
				economic and social development of the Sultanate in the last 41 
				years. <br>
