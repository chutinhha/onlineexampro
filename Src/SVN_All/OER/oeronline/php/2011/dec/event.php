				modernisation is paying rich dividends in the economic and 
				social wellbeing and development of the Sultanate. It captures 
				the impact of global economic outlook and regional developments 
				during the year 2010-2011 on Oman. In addition, it looks at the 
				opportunities and challenges for Oman’s society and economy. The 
				book does a detailed review of the path-breaking steps being 
				taken up by Oman to meet the changing needs of the polity and 
				market. It presents thought provoking critical analyses on 
				relevant subjects by domain experts and key national icons and 
				symbols which are uniquely identified with Oman. It also 
				features messages from the senior ministry officials apart from 
				showcasing the major achievements in prominent sectors including 
				Manufacturing, Trade &amp; Commerce, Oil &amp; Gas, Banking &amp; Finance, 
				Insurance, Capital Market, Healthcare, Education, Power &amp; Water, 
				Telecom, IT, Retail, etc.<br>
