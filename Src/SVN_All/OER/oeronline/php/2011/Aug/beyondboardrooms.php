						Blazing a trail</font></b><p align="justify" class="style3">
				<span class="style5"><strong>Backed by 30 years of experience in 
				tourism industry in Germany, Switzerland and the US, Manfred 
				Schweizer, CEO/ general manager, Arabian Trails is exploring the 
				possibility of giving visitors to the Sultanate of Oman an 
				authentic Arabian experience. Muahmmed Nafie reports<br>
				<br>
				</strong>
				</span>
				<br>
				<font face="Verdana" size="1">
				<img border="0" src="images/bey1-aug.jpg" style="border:2px solid #C0C0C0; float: right" class="style4" width="150" height="168"></font>Manfred 
				Schweizer fell in love with Oman during his first visit to the 
				country two years ago. He was so impressed by the Sultanate and 
				its people that he kind of joked that he should come back to 
				work and build up something here. After a couple of months, when 
				his partner- to-be Yahya Al Abrawi called up him to discuss some 
				new business opportunities, Manfred, who was then working with 
				Switzerland-based TUI, did not take it seriously. But the 
				opportune time came in the beginning of 2010 when Manfred 
				decided to quit TUI as a result of some structural changes in 
				the company. The idea of Arabian Trails as a Destination 
				Management Company (DMC) for the Gulf region and the Middle East 
				came to fruition in September 2010 after a few brainstorming 
				sessions between the two in the Sultanate and Switzerland.<br>
				<br>
				Arabian Trials, the DMC division of Gulf Sindbad Travel &amp; 
				Tourism, focuses mainly on presenting an authentic Arabian 
				experience for tourists to Oman. “It is kind of a new way of 
				tourism here. We provide our clients with an authentic Arabian 
				experience by bringing them together with the locals and give 
				them a better understanding of their life, culture and 
				tradition,” says Manfred.<br>
				<br>
				Within a short span of time, the idea has attracted a lot of 
				interest. Apart from leisure tourism, Arabian Trials has also 
				specialised in meetings, incentives, conferences and exhibitions 
				(MICE) tourism. Manfred believes that in the coming two to four 
				years, Oman has huge tourism potential mainly on MICE business 
				especially when Omran’s new conventional centre becomes 
				operational. He is also of the opinion that Oman’s national 
				carrier Oman Air’s recent foray into various international 
				destinations will give a fillip to the tourism sector and add to 
				the inflow of foreign tourists to the country.<br>
				<br>
				Manfred who is now deeply involved in building up the new 
				company and aspires to make Arabian Trails an industry leader in 
				the region. It has already set up its office in Dubai and eyes 
				expansion to the entire gulf region and other Arabian countries. 
				He also revealed that the company is planning to open its office 
				in Salalah in a few months to tap the potential of the 
				Sultanate’s celebrated tourism hub.<br>
				<br>
				The second brand of Gulf Sindbad Travel &amp; Tourism is Sindbad 
				Trails which is meant for the local market. With this brand, the 
				company is looking to introduce itself as a new tour operator 
				offering some unique products such as holiday packages and round 
				trips.<br>
				<br>
				<b><font size="2" color="#000080">Passionate traveller</font></b><br>
				<br>
				Manfred who had studied tourism in Germany and started his 
				carrier in 1972, worked for some internationally reputed tour 
				operating companies including Kuoni international, the biggest 
				international operator in Switzerland. Visiting new countries 
				has always been a passion for him; he has travelled extensively 
				and made a wide network world over. Brazil is one of his 
				favourite travel destinations. He also loves swimming, biking 
				and walking. An avid reader and a lover of classical Music, 
				Manfred is a fan of Paulo Coelho, the Brazilian novelist. <br>
