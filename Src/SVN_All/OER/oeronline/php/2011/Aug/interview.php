						<p align="justify" style="margin: 3px 10px">
						<span class="style27"><strong>Take off stage</strong></span><p class="style15">
				<strong><span class="style14">As India gets ready to celebrate 
				its Independence Day on August 15, 2011, HE Anil Wadhwa, 
				Ambassador of India to the Sultanate of Oman shares his views on 
				economic and commercial relations between the two countries. 
				Excerpts from an interview with <i><font color="#808080">Mayank 
				Singh</font></i></span></strong><p class="style15">
				&nbsp;<p align="center" class="style13">
				<font face="Verdana" size="1">
				<img border="0" src="images/intervu1-aug.jpg" width="150" height="169" style="border: 1px solid #808080; " align="left"></font><p align="justify" class="style13">
				<b>What have been the highlights of India-Oman bilateral 
				relations in the last one year?<br>
				</b><br>
				Indo-Oman bilateral relations have seen a rapid stride during 
				the last year. Some of the major highlights of the year were the 
				signing of the final documents related to the India-Oman Joint 
				Investment Fund in July 2010 and signing of the Final Report of 
				the Higher Committee on Economic Cooperation between India and 
				Oman. The former deals with discovering and exploring investment 
				opportunities in both countries while the later has laid out a 
				road map to enhance and intensify economic and commercial 
				relations between the two countries by identifying nine areas of 
				cooperation. These areas are: agriculture, healthcare, 
				infrastructure, tourism, chemicals and fertilizers, education, 
				oil &amp; gas, power and mining.<br>
				<br>
				A Memorandum of Understanding (MoU) on cultural cooperation was 
				also signed last year. The MoU will establish formal routes 
				between the two countries to cultivate close cultural 
				cooperation by exchange visits of artists, academicians and 
				cultural troupes. The MoU also covers the exchange of historical 
				manuscripts, identification of historical monuments, etc. 
				Government of India has decided to export 40,000 tonnes of wheat 
				and 10,000 tonnes of non-basmati rice to Oman. Earlier, Oman 
				raised this issue during the visit of Indian Minister of 
				Commerce &amp; Industry Anand Sharma to Muscat at the 6th session of 
				India-Oman Joint Commission Meeting in September 2010. Thus, 
				India and Oman are cooperating to enhance food security. 
				Overall, our bilateral relations are steady and moving ahead 
				towards a strategic relationship.<br>
				<br>
				<b>Bilateral trade between the two countries has witnessed fast 
				paced growth in the last couple of years. What has contributed 
				to this phenomenal growth over the years?</b><br>
				<br>
				Bilateral trade between the two countries crossed $4.5bn during 
				the Indian financial year 2009-10 (April 2009 – March 2010). 
				According to the latest figures from Oman, bilateral trade is 
				around $3.5bn in 2010, but this does not include figures of oil 
				&amp; gas exports from Oman to India for the second half of 2010. 
				India ranked as the fifth largest source of imports into Oman 
				last year. In addition to this India was the second biggest 
				importer of Omani non-oil exports and third largest importer of 
				Omani crude oil.<br>
				<br>
				Oman’s recent focus on non-oil exports has given a major thrust 
				in increasing exports from Oman. A study by the then OCIPED for 
				strategic marketing in India revealed that the total Omani 
				exports to India have shown rapid growth from RO 133mn in 2005 
				to RO911mn in 2009. Studies indicate even better performance in 
				the first half of 2010, with total Omani exports of RO779mn over 
				the six months alone. OCIPED identified 21 goods of Omani origin 
				for exports to India.<br>
				<br>
				On the other hand, Indian products are achieving a brand name 
				across the globe and ensure entry in every possible market. 
				Close cultural relations between the two countries are among 
				other driving forces which are continuously increasing 
				commercial engagements between them. India which earlier used to 
				export mainly food items has also diversified its trade basket 
				by exporting machinery, electronic goods and equipments, 
				machinery, auto mobile, consultancy services, training, etc.<br>
				<br>
				<b>Which are the sectors that hold promise for two way trade and 
				why?</b><br>
				<br>
				Oman and India cooperation is to ensure overall development of 
				the economies of the two countries and this will be done through 
				enhanced bilateral cooperation in two way investment, technical 
				assistance and identification of new areas of cooperation. The 
				Higher Committee has already identified nine areas of 
				cooperation which I have already mentioned. <br>
				<br>
				India’s move towards energy security is searching new markets to 
				get additional supply of crude oil. Oman’s move to supply its 
				crude to India resulted in export of 36.5 bbl crude oil to India 
				in 2010. In coming years, the focus will be on expanding 
				cooperation in developing medical facilities, food security, 
				fertiliser plants and investments in infrastructure sector, etc. 
				Higher education and vocational training are other areas where 
				India and Oman are moving closer. India is already offering 50 
				slots annually for Omani personnel under its International 
				Technical Training Programme. During the 6th session of JCM, the 
				Omani side had shown keen interest in developing fertiliser 
				plants in India. An impressive Indian presence, around 573,000 
				Indians working Oman, ensures close commercial engagement 
				between the two countries. <br>
				<br>
				<b>Your Excellency has been instrumental in initiating a number 
				of measures (like the India-Oman Joint Investment Fund) that 
				hold a lot of promise, can you share some of these initiatives 
				and how these have shaped up?</b><br>
				<br>
				The foundation of India-Oman Joint Investment Fund was laid 
				during the visit of Dr Manmohan Singh, prime minister of India 
				to Oman in November 2008. During this visit, a MoU was signed 
				between State Bank of India (SBI) and State General Reserve Fund 
				of Oman (SGRF) to set up a Joint Investment Fund to make equity 
				investments in various economic sectors of both the countries. 
				Final documents related to the establishment of the Fund, were 
				inked in July 2010 during the visit of the then Omani Minister 
				for National Economy HE Ahmed bin Abdulnabi Macki to India. The 
				joint venture started operations in February 2011 with an 
				investible corpus of $100mn (going up to $1.5bn) to be 
				contributed equally by SGRF and SBI. The fund is being operated 
				by a joint management company headquartered in Mumbai. The fund 
				is exploring opportunities in all sectors without any specific 
				preference. <br>
				<br>
				<b>Foreign Direct Investment between Oman and India has been on 
				the rise with Indian companies investing in the Sultanate and 
				Oman’s public and private sector doing the same in India. Do you 
				foresee bilateral FDI growing in future?</b><br>
				<br>
				Given the geographical proximity, India and Oman are close 
				partners in economic activities. Many large Indian companies 
				have already established themselves in Oman, and some, for 
				example, L&amp;T Modular Fabrication Yard and Jindal Shadeed Iron &amp; 
				Steel in Sohar and OMIFCO – a fertiliser joint venture in Sur- 
				have large commercial operations. The recent commissioning of 
				six mmtp Bharat Oman Refinery Limited in Bina, India is an 
				example of a successful Omani investment into India. Besides, 
				there are many examples which reflect the gradual increase in 
				two way investments. After the 6th JCM, there are many projects 
				under examination which will result in further big ticket 
				investments. <br>
				<br>
				Since Oman is a member of the GCC and has an FTA with the US, 
				setting up Indian manufacturing units in Oman is attractive for 
				Indian industries. GCC has emerged as a largest trading partner 
				for India and India is trying to diversify its trade basket with 
				the GCC countries. If Oman could invite more investment from 
				India it will be a win-win situation for both countries as it 
				will provide them more items for exports in order to move away 
				from a hydrocarbon based economy and on the other hand Indian 
				industry will get a new market in the GCC region. A lot of 
				potential is yet unexplored in the field of manufacturing 
				between the two countries and we need to develop cooperation in 
				setting up joint manufacturing units which could in turn provide 
				a better and profitable market for industries of the both 
				countries. <br>
				As of July 2010, there more than 1,500 Indo-Oman joint ventures 
				in Oman. Besides this, there are 140 large Indian companies 
				present in Oman and around 30 Omani companies that have 
				operations in India. India and Oman have lot to offer to each 
				other especially sharing each other’s experience in various 
				sectors such as food security, medical tourism, agriculture, 
				petro-chemicals, pharmaceuticals and science and technology.<br>
				<br>
				India and Oman could also work to accelerate cooperation between 
				SME sectors of the two countries. Therefore, I am confident that 
				bilateral investment is set for a boom in years to come.<p align="center" class="style13">
