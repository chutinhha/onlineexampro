						<td valign="top">
						<p align="justify" style="margin: 3px 10px">
						<span class="style27"><strong>
						<a name="TYERS">Broadening horizons</a></strong></span><p class="style15">
				<font face="Verdana" size="1"><strong><span class="style14">
				New Zealand is looking to explore new avenues of mutual 
				cooperation with Oman and the entire GCC as a whole, says
				<font color="#808080"><i>HE Rodney Harris</i></font>, 
				Non-resident Ambassador of New Zealand to Oman in an interview 
				with Muhammed Nafie<br>
&nbsp;</strong></span></font><p class="style15" style="text-align: center">
				<img border="0" src="images/face2face1-aug.jpg" width="150" height="206" align="left" style="border: 2px solid #C0C0C0"><p class="style15">
				Despite being down under, New Zealand has never been far away 
				for Omanis as they enjoy a visa on arrival whenever they want to 
				fly to the land of ‘the long white cloud’, since 1999. Moreover, 
				over 300 Omani students pursue their higher education in various 
				universities in the island nation. HE Rodney Harris, 
				non-resident ambassador of New Zealand to Oman, considers this 
				as a gesture of the deep and friendly relationship between the 
				two countries. He describes Omanis and New Zealanders as 
				likeminded people having much in common regardless of the 
				geographical distance separating the two countries.<br>
				<br>
				“One important aspect of our friendly relationship is that New 
				Zealand does not require a Visa from Omani nationals travelling 
				to New Zealand for 90 days,” says HE Harris. “It is just a 
				symbol of how much we respect Omanis and the warm welcome they 
				are getting in our country. We are very selective about the 
				countries that we give the visa-free status to and Oman is one 
				of them. And I am happy that Oman has been able to reciprocate 
				by extending the same status to New Zealanders travelling to 
				Oman. It speaks a lot about the mutual trust and friendly and 
				open relationship between the two countries.”<br>
				<br>
				HE Harris who has recently visited Muscat to explore new 
				opportunities in all spheres to build on and deepen this 
				relationship feels that the relationship between New Zealand and 
				the GCC countries, and Oman in particular, has become closer and 
				friendlier over time. He says the tenor of such a positive 
				change is noticeable in a number of recent initiatives whether 
				it be the moves to increase economic cooperation and create more 
				investment opportunities between the two sides on one hand or 
				several high-level contacts at various ministerial levels. He 
				thinks that the recent four-day visit to Saudi Arabia by New 
				Zealand foreign Minister Murray McCully was a significant step 
				towards augmenting these relations. “It is time to look out for 
				deeper cooperation between New Zealand and the GCC as an 
				institution,” he says. “There is a lot more things we can do to 
				improve the bilateral relations at economic, social and 
				political spheres. The embassy in Riyadh is looking at each gulf 
				country to understand what they are trying to achieve socially, 
				economically and politically and what New Zealand can do to help 
				them; and what they can offer to us as well”.<br>
				<br>
				<font color="#000080" size="2"><b>Free Trade Agreement</b></font><br>
				<br>
				Although HE Harris took up the current assignment only in August 
				2010, his relationship with Oman and the region as a whole 
				begins even before that. He was part of the New Zealand team for 
				negotiating a Free Trade Agreement (FTA) with the entire GCC. 
				New Zealand was one of a very few countries that have finalised 
				negotiations for FTA with the GCC as a region. Harris says Oman 
				has played a significant role in the negotiation and helped 
				greatly in bringing it into a peaceful conclusion. “The 
				negotiation was concluded in 2009 and I am happy that we have 
				come out with a good agreement. It has not been signed yet as it 
				takes time for an FTA in which seven countries are involved to 
				come into force. But I am optimistic it will be signed very 
				soon.” He also adds that the FTA will be greatly instrumental in 
				removing trade barriers between the New Zealand and the 
				six-nation GCC to the benefit of all seven economies which have 
				a joint trade volume of $ 2.5bn.<br>
				<br>
				<font color="#000080" size="2"><b>Educational relationship</b></font><br>
				<br>
				New Zealand eyes a lot of opportunity in the constant drive by 
				the Omani government to improve the education sector by creating 
				more educational opportunities for Omani nationals and in 
				similar initiatives by other GCC countries. “In this regard, 
				there isa lot we can do, have done and will do in Oman and other 
				GCC countries,” he avers. There are around 300 Omani students 
				studying on Omani scholarship in various Universities in New 
				Zealand. They have a very good reputation for coping very well 
				with a tough curriculum and course, studying hard and making 
				good friends with the New Zealanders. <br>
				<br>
				Elaborating further on the relationship between the two sides on 
				education, HE Harris says, “There are roughly 8,000 students in 
				New Zealand coming from various GCC countries. Almost 7,000 of 
				them are from Saudi Arabia. Besides, there are a lot of students 
				from Egypt and various other Middle East countries opting for 
				New Zealand as their favourite destination for higher studies. 
				This provides Omani students travelling to New Zealand for 
				higher education a homely feeling as they can find a lot of Arab 
				students hailing from their cultural and religious backgrounds 
				and can form their own communities there. They are more welcome 
				to New Zealand.”<br>
				<br>
				He is of the opinion that Omani students coming back from New 
				Zealand stand a better chance of being considered for various 
				responsible posts both at government and private sectors.<br>
				<br>
				Besides, New Zealand educational institutions and experts have a 
				good track record in Oman and other GCC countries as they work 
				closely with governments and various institutions in the region 
				to improve the education system as well.<br>
				<br>
				<font color="#000080" size="2"><b>Diversifying cooperation</b></font><br>
				<br>
				HE Harris who had a series of conversation with various senior 
				government officials and representatives of the private sector 
				during his visit to Oman finds that there are a lot of 
				opportunities of mutual cooperation. He believes that New 
				Zealand can significantly contribute to the development Oman’s 
				fisheries industry. “It is not New Zealand telling Oman that you 
				should do this or that. As Oman’s aquaculture industry has a 
				huge potential for growth, Oman can find a good partner for this 
				sector’s development in New Zealand which has a lot of expertise 
				in this area. With Oman poised to diversify its economy, create 
				more job opportunities and increase its nominal export, 
				aquaculture is an obvious place to start with. We already have a 
				consultant who is working closely with the government and the 
				business sector in this regard.”<br>
				<br>
				HE Harris who made his fourth visit to Oman says that the more 
				he visits Oman the more opportunities for mutual cooperation he 
				could realise. During this visit he explored the prospect of New 
				Zealand’s manufacturers basing themselves in Oman as a gateway 
				not only to the GCC but other countries such as India. He also 
				suggest that New Zealand’s expertise in construction sector can 
				be used for building factories in Oman thereby giving employment 
				opportunities for Omanis. He also informs that the newly formed 
				New Zealand-Middle East Business Council comprising 50 New 
				Zealand companies from all sectors seeks to strengthen the 
				cooperation and open up new areas for diversifying the export to 
				the region away from agriculture and food products.<br>
				<br>
				A little known fact is that New Zealand, which has a very large 
				mining industry, is also a net exporter of oil; it exports crude 
				oil and imports refined oil. HE Harris suggests that since it is 
				very hard to extract oil from New Zealand, it can make best use 
				of Oman’s expertise in enhanced oil recovery.<br>
