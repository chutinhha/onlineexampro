	<tr>
		        <td valign="top" width="100%"> 
				<p align="justify" style="margin: 10px; ">
					<b><font face="Verdana" size="4" color="#C00000">
				<img border="0" src="images/jan_lnationalecnomy1.jpg" hspace="8" vspace="5" style="border: 2px solid #C0C0C0; " align="right" width="150" height="157"></font></b><p class="MsoNormal">
				<b><font face="Verdana" size="2">The Sultanate has realised 
				significant achievement despite the ever-changing regional and 
				global conditions due to the comprehensive Five Year Development 
				Programmes, says HE Ahmed bin Abdulnabi Macki, Minister of 
				National Economy and Deputy Chairman of Financial Affairs and 
				Energy Resources Council<br>
				<br>
				</font></b><font face="Verdana" size="1">The government’s basic 
				goal, since the beginning of the Blessed Renaissance, was to 
				achieve growth and progress that comprises the economical and 
				social dimensions reflecting directly and positively on the 
				Omani citizen by way of raising the standard of living and 
				providing a better life and well-being for him and his family.<br>
				<br>
				To achieve this, the government adopted a comprehensive 
				development concept based on free market and perfect competition 
				to activate and stimulate the national economy, raise its 
				efficiency and growth rates. It also, followed indicative 
				planning to rationalise the economic path, maximise the social 
				returns of growth, and improve the quality of life of its 
				citizens through the Five Year Development Plans.<br>
				<br>
				It is worthwhile to note that the investments between 1971-1975 
				were only RO395.6mn and in the first six Five-Year Plans 
				1976-2005, the investments amounted to RO7.61bn. However, in the 
				Seventh Plan until September 2010, the investments were RO5.19bn 
				i.e. 64 per cent of the total investments during 35 years from 
				1971 until the end of 2005.<br>
				Under the wise and inspiring leadership of His Majesty Sultan 
				Qaboos bin Said, there was continuous follow-up and close 
				monitoring, directing and making sound and crucial decisions of 
				the development plans. The government is considered the prime 
				mover of economy where it undertakes the implementation of most 
				development projects whether production or service. It is 
				assisted by the oil revenues, which are optimally utilised to 
				realise development and welfare of citizens. In spite of the 
				fluctuations in oil prices over time, the government was able to 
				move forward in implementing development projects even during 
				the decline in oil prices benefiting from reserves it 
				accumulated during high prices period.<br>
				<br>
				</font><font face="Verdana" size="2" color="#000080"><b>Civil 
				ministries development</b></font><font face="Verdana" size="1"><br>
				<br>
				The development programme of civil ministries and government 
				units represents one of the main executive tools to realise its 
				objectives, policies and implementation of projects included in 
				the successive Five-Year Development Plans. The cited plans were 
				formulated according to requirements and needs of each stage 
				after a thorough assessment of the previous Five-Year Plans. The 
				total government expenditure during the previous four decades 
				amounted to RO94bn taking in account equitable regional 
				distribution of the outcomes of the development. Services like 
				education, health, electricity, water, roads, communication were 
				spread in various governorates and regions of the Sultanate.<br>
				<br>
				</font><font face="Verdana" size="2" color="#000080"><b>
				Spreading the development</b></font><font face="Verdana" size="1"><br>
				<br>
				Altogether seven Five-Year Development Plans were implemented 
				with the Eighth Five-Year Development Plan commencing from this 
				month. All these plans sought the achievement of the long-term 
				strategic plan, which was carefully prepared with clearly 
				defined objectives, policies and mechanisms needed for 
				implementation.<br>
				<br>
				The Five-Year Development Plans were characterised by wide 
				participation of the various community entities so that their 
				directives meet the ambitions of all society groups and sectors. 
				The different wilayats, governorates and regions participated in 
				identifying their needs for primary services by providing the 
				sectoral ministries with their requirements which would be taken 
				into consideration when preparing the sectoral plans. In 
				addition, groups of specialists from different ministries 
				conduct field visits to the wilayats to identify their real 
				needs. In addition, the Royal Tours in which His Majesty 
				annually visits a number of wilayats to meet and have dialogues 
				with his citizens about their issues and needs contributed in 
				defining the citizens’ needs and their immediate fulfilment.<br>
				<br>
				</font><b><font face="Verdana" size="2" color="#000080">
				Two-stage growth</font></b><font face="Verdana" size="1"><br>
				<br>
				Oman’s development path, during the four decades, passed through 
				two important stages. The first from the beginning of the 
				Renaissance in 1970 and continued for 25 years until the 
				beginning of 1996. During this period, the Sultanate was able to 
				set up successively the basic pillars for economical and social 
				transformation. The second stage from 1996 until now which 
				manifests the beginning of the launch to the future horizons, 
				realises the sustained self-growth and full readiness for deeper 
				and wider link with the global economy and its surrounding 
				significant political and economical challenges.<br>
				<br>
				The second stage started with the preparation of the long-term 
				development strategy (1996-2020) which anticipates the 
				development vision for the next 25 years. Within the context, of 
				this second stage, the preparation and implementation of the 
				successive Five-Year Plans have been achieved since 1996. The 
				vision defined the path of the Omani economy for 25 years in 
				four main dimensions. Each dimension constitutes a number of 
				objectives to be realised. <br>
				<br>
				<b>The four dimensions are as follows: </b></font></p>
				<ol>
					<li>
					<p class="MsoNormal"><font face="Verdana" size="1">Realising 
					sustainable comprehensive economic development within a 
					stable economic framework</font></li>
					<li>
					<p class="MsoNormal"><font face="Verdana" size="1">Advanced 
					human resources development so that the Omani citizen is 
					enabled to undertake his full role in national economy</font></li>
					<li>
					<p class="MsoNormal"><font face="Verdana" size="1">
					Development and diversification of the production base of 
					the Omani economy within a dynamic and interactive framework 
					with the global economy</font></li>
					<li>
					<p class="MsoNormal"><font face="Verdana" size="1">Creation 
					of an efficient and effective private sector that is capable 
					of participating in the economical and social development 
					process and able to compete globally Eighth five-year plan</font></li>
				</ol>
				<p class="MsoNormal"><font face="Verdana" size="1"><br>
				<b>The implementation of the Eighth Five-Year Development Plan 
				begins this month until the end of 2015. The most salient 
				dimensions and directives of the Plan include: </b></font></p>
				<ol>
					<li>
					<p class="MsoNormal"><font face="Verdana" size="1">
					Maintaining the macroeconomic stability and economical 
					balance that ensure the steady growth in GDP and increase 
					per capita income.</font></li>
					<li>
					<p class="MsoNormal"><font face="Verdana" size="1">
					Maximising the social return of development in a way that 
					positively reflects on citizens life through upgrading of 
					education quality, expansion in higher education, 
					improvement in the quality of health services and its close 
					vicinity to citizens, creation of more employment 
					opportunities for citizens and youth in particular.</font></li>
					<li>
					<p class="MsoNormal"><font face="Verdana" size="1">
					Continuance in the economic diversification policy, 
					strengthening the manufacturing fields in various regions of 
					the Sultanate, development of small and medium size 
					projects, increasing non-oil exports, improving its quality 
					level, and developing its structure.</font></li>
					<li>
					<p class="MsoNormal"><font face="Verdana" size="1">
					Attracting more national and foreign investments, enhancing 
					the private sector role in the development process 
					especially after setting favourable macroeconomic 
					environment, upgrading laws and legislations related to 
					investment and provision of various incentives.</font></li>
					<li>
					<p class="MsoNormal"><font face="Verdana" size="1">Dealing 
					with the rapid regional and global economic variables to 
					ensure benefit from the positives and opportunities 
					available, limiting the negative impacts of these variables 
					and maintaining the economical and strategic interests of 
					Oman. <br>
					Excerpts taken from HE Macki’s speech given on the occasion 
					of the 40th National Day.</font><font face="Verdana" size="2"><b><br>
&nbsp;</b></font></li>
				</ol>
