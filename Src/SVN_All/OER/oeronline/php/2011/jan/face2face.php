		        <td valign="top" width="100%"> 
                  <p align="justify" style="margin: 3px 10px"> 
					<b><font face="Verdana" size="1" color="#C00000">&nbsp;</font><font face="Verdana" size="4" color="#C00000"><br>
					</font><font face="Verdana" size="4" color="#C00000">Crisis 
					over, uncertainty remains<br>
                    </font></b><font face="Verdana" size="1"><br>
					</font><b><font face="Verdana" size="2">Dr </b><i>Montek Singh 
					Ahluwalia</i><b>, Deputy Chairman of Planning Commission of 
					Government of India says uncertainty, driven primarily by 
					Euro zone, loomseverywhere as we enter the new year, in a 
					chat with Akshay Bhatnagar</b></font></p>
					<p align="justify" style="margin: 3px 10px"> 
					&nbsp;</p>
                  <p align="center" style="margin: 3px 10px"> 
					<img border="0" src="images/jan_face2face.jpg" width="150" height="212" align="left" style="border: 2px solid #C0C0C0"></p>
                  <p align="justify" style="margin: 3px 10px"> 
					<font face="Verdana" size="1"><b>Do you think the global economy is out of the woods?<br>
					</b><br>
					Well, it’s not completely out of the woods, but it’s 
					certainly true that we have arrested the worst outcome that 
					people feared – something like the Great Depression. We knew 
					that the growth will not come back strongly in the 
					industrialised world. In some way, the growth of the 
					emerging markets is better than what it was a year ago. The 
					only problem is that the sovereign debt in some of the 
					industrialised countries is deeper than what we thought 
					earlier. So to an extent, we are not sure how it will play 
					out. <br>
					<br>
					In some ways, the Euro zone presents a very special problem. 
					If you have fiscal and debt problems in a currency union, it 
					becomes especially difficult because for the viability of a 
					currency union it is assumed that individual components will 
					behave themselves fiscally. Uncertainty on its own is 
					greater than what people would have said a year ago.<br>
					<br>
					So, in short, the growth of emerging markets is better than 
					what people expected. The global economy is out of the 
					woods. The emerging markets are doing well but it is very 
					important for them to show stability and there lies the main 
					problem. The responsibility is on the industrialised world 
					to restore stability even if their growth rate is not that 
					high. However, there is always uncertainty. For India and 
					other emerging countries in Asia, the growth prospects look 
					good. However, uncertainty looms everywhere because we do 
					not know what the coming months will produce.<br>
					<br>
					<b>What have been the positive outcomes of the global 
					economic crisis?<br>
					</b><br>
					The positive outcome from India’s point of view is the 
					establishment of confidence that our growth is reasonably 
					strong. Yes, we are linked to the world economy and we did 
					get impacted. But the fact that after India’s economy got 
					bumped from 9 per cent to 6.7-7 per cent growth rate, India 
					has shown a lot of resilience, if we compare our current 
					growth rate. <br>
					<br>
					The second outcome is the shift of the global centre of 
					gravity towards Asia. We hope it will be a dispersed shift. 
					China definitely is the leading economy but others are also 
					pulling it along. The world economy will be more multi 
					polar. But the difficulty is that I am not sure whether we 
					have the governing institution that can cope with the 
					reality of a multi polar economy. The governing structures 
					are still based on the assumption that the industrialised 
					countries are the leaders and that’s how they exist in 
					institutions. But the world is changing. G8 has changed to 
					G20 and many emerging economies are now a part of it. But it 
					is too early to predict whether this grouping will become 
					functionally effective. For countries to discuss their 
					policies and modify them, as required, need a much evolved 
					act of global responsibility. Most countries invoke 
					sovereignty that we will do what we have to do. I will not 
					be too pessimistic because the G20 has only been there for 
					two years and it takes a lot of time to build confidence. I 
					would say that the creation of G20 with all its weaknesses 
					is a positive outcome.<br>
					<br>
					The other major outcome is that a process of financial 
					sector reforms has been started. Now trying to alter the 
					regulatory structure to control risk is easier said than 
					done. These things are not done by regulations alone; they 
					are done by supervisory philosophy and the effectiveness of 
					execution. But it has definitely put a halt on the kinds of 
					problems that can arise as a result of excessive risk 
					taking. What it will imply to the world is yet to be sorted 
					out because a part of the problem is when you change your 
					regulatory structures, there will be a shrinking in the 
					financial activities in the industrialised world. Whether 
					this shrinking will be excessive or discriminating against 
					the developing countries are the issues we have to worry 
					about. In my opinion, from India’s point of view, we were 
					never interested in short-term objectives. We were rather 
					interested in long-term capital and foreign direct 
					investment (FDI), etc. <br>
					<br>
					In the next several years, the gap between the growth of 
					potential output between India and the US and Europe will be 
					larger. This should redirect global investment flows towards 
					India. This is a fast growing part of the world and this is 
					where the money should go. But we have to keep a watch on 
					the prevalent uncertainty which could play spoil sport. <br>
					<br>
					<b>What is your viewpoint on Obama’s performance on the 
					economic front in the US so far?<br>
					</b><br>
					Well, he has an extraordinarily difficult job because there 
					is a mismatch between what is feasible and what is expected. 
					The problem is the expectations. The US has been used to of 
					long years of prosperity and suddenly the country faces an 
					unusual unemployment rate of 9.6 per cent. People expect 
					this issue to be resolved in two weeks! All the data we have 
					and all the studies suggest that if you get hit by a 
					financial crisis then it takes a few years to recover. Early 
					recovery is not feasible. There is a lot of debate going on 
					in the US about this. Obviously, one of the issues is that 
					if it is going to take that long, should they continue or 
					discontinue the stimulus. Some very distinguished economists 
					say they should continue. Well, I am told, policy making is 
					the art of the possible. Politically, I don’t think that the 
					Congress will support a fiscal stimulus so the only stimulus 
					they can do is monetary. Many economists feel that monetary 
					stimulus under the current circumstances may not work. We 
					also know that he has to handle the politics of the 
					Congress, so let’s just wish him well. <br>
					<br>
					It is very important to understand that it is in our 
					interest that the US economy rebounces quickly as it is 
					dominated by a sort of self confident private sector. No 
					questions about that. It is a win-win situation if the 
					President can get it done. On the other hand, it is also 
					true, for the first time, that just because the US sneezes, 
					the world does not catch cold. That does not mean the US is 
					not important. The US is very important as they are the 
					largest economy in the world. It is a major source of 
					innovation and technology. I think we all have an interest 
					in that and we all wish the President well.<br>
					<br>
					<b>What have been the learnings for the Middle East from 
					this crisis?<br>
					</b><br>
					One thing they should learn is that in the end, the capital 
					should flow where the growth rates are high. And they have 
					traditionally viewed the flow of capital to the US and the 
					Western countries as the safest method of deployment. People 
					who happily invested in many of these instruments earlier, 
					when they look back on it could construe that the Indian 
					investments could have fared much better than the 
					investments they had in these institutions. Looking ahead 
					the most important lesson is that invest your surplus 
					capital where rapid growth is taking place, like India. <br>
					<br>
					<b>What are your expectations in 2011 for the global 
					economy?<br>
					</b><br>
					I am not an expert in making predictions, yet most people 
					think that 2011 will see a slower growth than 2010 because 
					the previous year was a rebound from a very low base. There 
					is still uncertainty as to whether the Euro zone will create 
					a problem. Most of the projections of 2011 for the 
					industrialized countries suggest that growth will be 
					slightly slower. We, in India, think it will be better. We 
					projected a growth rate of 8.5 per cent for 2010-11 and we 
					are doing a little better than that. For the year 2011-12, 
					we are hoping to get 9 per cent. Again we see that better 
					managed emerging market economies are doing well, while the 
					industrialized countries are struggling. In the US, it is 
					coming along as expected, so it’s hardly a problem. It is 
					the Europe which is a little uncertain.<br>
					<br>
					<b>There have been speculations that you are among the 
					frontrunners for the top position at the International 
					Monetary Fund (IMF). What is your reaction to it?<br>
					</b><br>
					This is pure speculation and there is nothing concrete in 
					it. There is no word that the current MD of IMF is leaving. 
					He is doing a good job. I am very happy with what I am 
					doing. <br>
