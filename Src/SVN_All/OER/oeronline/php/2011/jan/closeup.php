					<font face="Verdana" size="1">
                    <b>Looking ahead </b><br>
					<br>
					However, it makes sense for GCC officials to evaluate the 
					Euro’s experience whilst pursuing the GMU project. The 
					European model continues to struggle with several member 
					countries, starting with Greece but not ending with Ireland, 
					reeling under consequences of debt overload. <br>
					<br>
					Certainly, the next GCC summit, scheduled for late 2011 in 
					Riyadh, will most likely result in the adoption of further 
					steps aimed at integrating regional economies. Originally, 
					Bahrain was due to host summit No 32, but reflecting 
					brotherly spirit among GCC nations, it accepted a Saudi 
					request for a change of venue.<br>
