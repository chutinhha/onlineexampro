                  <p align="justify" style="margin: 3px 10px"> 
					<b><font face="Verdana" size="4" color="#C00000"><br>
                    Vital results in Abu Dhabi<br>
                    </font><font face="Verdana" color="#C00000" size="2">&nbsp;</font><font face="Verdana" size="4" color="#C00000"><br>
                    </font></b><font face="Verdana" size="2"><b>The resolution 
					allowing GCC companies to open branches in member-states is 
					one of the significant outcomes of the 31st GCC summit</b></font></p>
                  <p align="justify" style="margin: 3px 10px"> 
					&nbsp;</p>
                  <p align="justify" style="margin: 3px 10px"> 
					<font face="Verdana" size="1">
                    The 
					31st summit of Gulf Cooperation Council (GCC) held in Abu 
					Dhabi in December achieved some notable results with regard 
					to the goal of integrating economies within the six-nation 
					group. The most outstanding accomplishment relates to 
					passing of a resolution allowing GCC companies to open 
					branches in member-states.<br>
					<br>
					More specifically, the agreement calls on the authorities to 
					treat branches of GCC firms as local. This very development 
					fosters completion of the Gulf Common Market (GCM) project, 
					which is regarded as a key component in integrating regional 
					economies.<br>
					<br>
					The GCM, which began in 2008, calls for the free flow of 
					factors of production among member states. The plan covers 
					all economic and investment services in addition to dealing 
					in the stock market and setting up companies in both the 
					public and private sectors.<br>
					<br>
					The 30th GCC summit held in Kuwait in 2009 added access to 
					technical studies to the list of shared activities. In 
					practice, the GCM focuses on finding a unified regional 
					market through which nationals could benefit from 
					opportunities in all member states.<br>
					<br>
					Although inter-trade amongst GCC countries remains far below 
					the expectations, there are promising prospects. Inter-GCC 
					trade amounts to $67bn, just above 5 per cent of total trade 
					(exports and imports) of the six-nation group which is 
					estimated at $1,334bn in 2010.<br>
					<br>
					The petroleum sector accounts for more than two thirds of 
					exports from GCC countries. It makes the contribution of 
					inter-GCC trade to the total GCC trade higher when oil 
					prices decline. Needless to say, oil is a strategic product, 
					whose price reflects a complex set of socio-economic and 
					socio-political factors.<br>
					<br>
					Saudi Arabia is the second largest oil producer in the world 
					after Russia, and the largest oil exporter in the world. 
					According to June 2010 version of BP Statistical Review of 
					World Energy, GCC countries control about 40 per cent of the 
					world’s proven oil reserves. Saudi Arabia alone holds 20 per 
					cent of global oil reserves.<br>
					<br>
					Another positive outcome of Abu Dhabi summit relates to 
					possible construction of a regional train link. More 
					specifically, the summit attendees called on concerned 
					parties to press ahead with preparing detailed designs on 
					the scheme.<br>
					<br>
					Fortunately, FIFA’s decision to grant Qatar the honour of 
					staging the World Cup in 2022 adds further logic for the 
					project. The tournament provides GCC countries at large with 
					the opportunity to be presented as integrated economies. 
					World Cup 2022 provides additional argument for 
					implementation of a causeway linking Qatar and Bahrain.<br>
					<br>
                    <br>
                    <b>Electricity grid<br>
					<br>
                    </b>At the same time, the summit gave GCC leaders the 
					opportunity to evaluate the progress of the regional 
					electricity grid. Started in 2010, the scheme’s first phase 
					involves all GCC countries except the UAE and Oman. At least 
					one GCC country offered electricity to another member state 
					suffering from a shortage during the peak of summer this 
					year.<br>
					<br>
					On the other hand, the summit paid only lip service to two 
					projects designed to strengthen the integration of GCC 
					economies, namely the customs union and Gulf Monetary Union 
					(GMU).<br>
					<br>
					Launched in 2003, the customs union scheme missed several 
					deadlines. The original plan called for full implementation 
					within two years. A pre-summit announcement suggested that 
					member states would satisfy requirements of the customs 
					union by 2011.<br>
					<br>
					In reality, the scheme encountered serious challenges from 
					its very inception, notably related to developing a formula 
					for fair distribution of customs duties among member 
					countries. Sticking points include ports of entry and final 
					destinations of goods.<br>
					<br>
					At the same time, the summit offered little clue to the 
					progress of the GMU project, and officially launched at the 
					start of 2010 but with no specific deadline. Only four GCC 
					countries – Saudi Arabia, Qatar, Kuwait and Bahrain – are 
					parties to the venture.</font></p>
					<p align="justify" style="margin: 3px 10px"> 
					<br>
					<font face="Verdana" size="1">
                    <b>Monetary union</b><br>
					<br>
                    <img border="0" src="images/jan_closeup.jpg" align="left" hspace="12" width="300" height="178" style="border: 1px solid #000000">The final goals include unifying monetary and fiscal 
					policies and therefore introducing a single currency for 
					member countries.<br>
					<br>
					Implementation of the monetary union project requires member 
					countries to adhere to five conditions dealing with fiscal 
					and monetary standards. These are: limiting public debt to 
					70 per cent of nominal gross domestic product (GDP) at 
					current prices; restricting budget deficits to 3 per cent of 
					GDP; limiting inflation rates plus 2 per cent; ascertaining 
					that short-term interest rates do not exceed 2 per cent the 
					average of three of the lowest rates of member countries; 
					and ensuring that reserves cover import bills for four 
					months.</font></p>
