		        <td valign="top" width="100%"> 
				  <p align="left" style="margin:3px 10px; " dir="ltr"> 
					<font size="1" face="Verdana, Arial, Helvetica, sans-serif">
					<br>
					</font><b>
					<font size="2" face="Verdana, Arial, Helvetica, sans-serif">
					Ports and free zones are emerging as the centrepieces of 
					Oman’s growth plans and reclaiming their past glory of being 
					the vital link between the trade routes of East and West. 
					Visvas Paul D Karra reports on the maritime movements in the 
					Sultanate</font><font size="1" face="Verdana, Arial, Helvetica, sans-serif"><br>
					</font></b>
					<font size="1" face="Verdana, Arial, Helvetica, sans-serif">
					<br>
					&nbsp;</font><p align="left" style="margin: 3px 10px" dir="ltr">
					<b>
					<font size="2" face="Verdana, Arial, Helvetica, sans-serif" color="#000080">
					Pièce(s) de résistance</font></b><br>
					<br>
					<font size="1" face="Verdana, Arial, Helvetica, sans-serif">
					The chronicle of Oman’s history, past and present, is 
					inevitably etched with its maritime exploits. In the days of 
					yore, Omani shipping activity was distinguished in the Gulf 
					by the greatness of its ships, and the importance of its 
					copper trade, which found receptive markets in the cities of 
					Mesopotamia (modern-day Iraq). Cuneiform texts link Majan 
					with a particular ship called Majan, considered as an 
					example of the greatest and most robust of ships. The 
					influential ports of Qalhat, Muscat and Sohar effectively 
					controlled shipping routes up and down the Gulf. Omani ports 
					rose to prominence in the 10th century when Omani merchant 
					fleets sailed to East Africa and Madagascar to buy ivory, 
					leopard skins, tortoise shell and other exotic goods, which 
					were then traded as far away as India and China in return 
					for spices, silks and porcelain.<br>
					<br>
					</font><b>
					<font size="2" face="Verdana, Arial, Helvetica, sans-serif" color="#000080">
					Natural choice</font></b><font size="1" face="Verdana, Arial, Helvetica, sans-serif"><br>
					<br>
					So when the policy makers of modern Oman went to the drawing 
					boards to strategise on how to diversify the national 
					economy from oil to non-oil resources, they had to look no 
					further than the sea. As the past linked up with the 
					present, the sea-faring status of Oman ushered in a new era 
					of maritime commerce and prosperity for the Sultanate making 
					ports and the attached free zones the centrepieces of the 
					country’s economic diversification plans.<br>
					<br>
					“The strategy to diversify into the maritime side is correct 
					because ports as a whole obviously are required for a 
					country’s economy to grow unless it is landlocked. And then 
					a free zone is a natural extension of that and an easy way 
					of doing business, attracting investments and generating 
					activity around its market. You could call this the seeds of 
					economic growth,” says Peter Ford, CEO, Port of Salalah.</font><p align="left" style="margin: 3px 10px" dir="ltr">
					&nbsp;<p align="center" style="margin: 3px 10px" dir="ltr">
					<font size="1" face="Verdana, Arial, Helvetica, sans-serif">
					<img border="0" src="images/jan_cover1.jpg" width="700" height="378"></font><p align="justify" style="margin: 3px 10px" dir="ltr">
					<font size="1" face="Verdana, Arial, Helvetica, sans-serif"><br>
					<br>
					The advantage for Oman is that it has three different ports 
					as of today, all with really different business models. Port 
					of Sohar and Port Sultan Qaboos focus heavily on the import 
					and export as the gateway cargo to and from Muscat and Sohar 
					areas and to an extent the UAE. Then you have Port of 
					Salalah, which has a small supporting consumer market with 
					no gateway to any place except to the rest of the world and 
					is placed among the top 20 transhipment hubs of the world. 
					“That is the most interesting thing: Essentially Oman is a 
					gateway to every single shipping location worldwide for 
					every shipping line. So you can take a box from any port in 
					Oman and be in India, East Africa or the US within days,” 
					says Ford who has vast experience in varied markets like 
					Jamaica, US and the Netherlands.<br>
					<br>
					</font><b>
					<font size="2" face="Verdana, Arial, Helvetica, sans-serif" color="#000080">
					Impressive performances</font></b><font size="1" face="Verdana, Arial, Helvetica, sans-serif"><br>
					<br>
					Up until 1988, Port Sultan Qaboos, earlier known as Mina 
					Qaboos, was the only docking point in Oman for traffic 
					carrying conventional cargo as well containers. Then the 
					Port of Salalah, (earlier known as Mina Raysut) down south, 
					was not only envisaged and developed as a general cargo 
					terminal but also expanded for container operations creating 
					the Middle East’s leading transhipment hub right in the 
					middle of East-West shipping lanes of the world overlooking 
					the Indian Ocean. The Port of Salalah has four of the 
					world’s largest shipping lines like MSC, CMA CGM, Maersk 
					Line and APL to call at Salalah.<br>
					<br>
					The third major port, the Port of Sohar, mooted in 2002, was 
					developed as an industrial port. This port is located 
					outside the Straits of Hormuz free from the geo-political 
					tensions of the region and gives a strategic comfort to 
					international investors. The industrial area located within 
					the port has attracted investments worth more than $15bn in 
					petrochemicals, metals and other manufacturing industries 
					and is part of the core of the Sohar Industrial Port Zone (SIPZ), 
					which includes a free zone.</font><p align="center" style="margin: 3px 10px" dir="ltr">
					<font size="1" face="Verdana, Arial, Helvetica, sans-serif">
					<br>
					<img border="0" src="images/jan_cover2.jpg" width="400" height="329"><br>
&nbsp;</font><p align="justify" style="margin: 3px 10px" dir="ltr">
					<font size="1" face="Verdana, Arial, Helvetica, sans-serif">As an indication of just how much economic gains are there, 
					the two so-called new kids on the block (Sohar and Salalah 
					ports) have between them notched up impressive performances 
					even while the rest of the world reeled under the spin-off 
					effects of the global financial crisis in 2008 and 2009. At 
					the Port of Sohar, the overall cargo throughput increased by 
					39 per cent from 6.9 million tonnes in 2008 to 9.7 million 
					tonnes in 2009 mainly from liquid and dry bulk and 
					containers.<br>
					<br>
					The general cargo terminal at Port of Salalah recorded a 
					record 2.92mn tonnes of throughput from January 2010 to the 
					end of June. This is up a staggering 66 per cent on the 
					figure recorded for the same period in 2009 - 1.762mn 
					tonnes. Unlike most ports around the world, throughput at 
					the Port of Salalah increased in 2009 despite the recession, 
					making these H1 2010 figures even more remarkable.<br>
					<br>
					Meanwhile, Port Sultan Qaboos together with the Salalah port 
					unloaded and loaded 6.4 million tonnes of cargo in the year 
					leading up to the end of July. According to data by Oman’s 
					national economy ministry, when compared to the previous 
					year the two ports handled 30 per cent more cargo.<br>
					<br>
					</font><b>
					<font size="2" face="Verdana, Arial, Helvetica, sans-serif" color="#000080">
					Setting the standards</font></b><font size="1" face="Verdana, Arial, Helvetica, sans-serif"><br>
					<br>
					According to HE Said Bin Hamdoon Al Harthy, undersecretary 
					for ports and maritime affairs, ministry of transport and 
					communications, Omani ports are not only attracting 
					investments into the Sultanate but they are also setting the 
					industry standards and norms for investments in the whole 
					region. He says all the ports are doing the best to attract 
					business as they can handle any types of shipping business 
					and serve the region through our feeder services. “Oman is 
					basically playing a major role in attracting international 
					investments into the region as we are setting the right 
					standards to attract any kind of business with 
					investor-friendly rules and regulations combined with 
					political stability,” HE Harthy adds.<br>
					<br>
					</font><b>
					<font size="2" face="Verdana, Arial, Helvetica, sans-serif" color="#000080">
					Logistics hub</font></b><font size="1" face="Verdana, Arial, Helvetica, sans-serif"><br>
					<br>
					“Oman is pitching itself as the logistic hub of the region 
					thanks to its locational advantage and proximity to the GCC 
					countries, Iran, Iraq, and the Indian subcontinent,” says 
					Saud bin Ahmed Al Nahari, the executive president of Port 
					Services Corporation (PSC), which operates the Port Sultan 
					Qaboos. <br>
					<br>
					Because of number of ports (Sohar, Muscat, Salalah and Duqm 
					in the future) as the main commercial and industrial ports 
					along with the special economic zones that will be developed 
					adjacent to these ports, encouraging the logistics business 
					is obviously an important factor in the scheme of industrial 
					development.<br>
					<br>
					Right on top of this agenda is the massive investments in 
					infrastructure. Firstly, the two existing airports at Muscat 
					and Salalah are being expanded for more capacity to handle 
					both freight and passengers while contracts to build new 
					airports at Sohar and Duqm have been awarded. Then we have 
					the proposed Al Batinah Railway development initially 
					linking Sohar with Barka where a container hub is being 
					proposed, which increases the potential of increasing 
					intermodal cargo transport options particularly in the Sohar 
					region.<br>
					<br>
					Meanwhile, the Port of Sohar is developing two large scale 
					projects which will transform the wider Batinah region into 
					a regional hub for industrial-scale mining, quarrying and 
					mineral processing activities. <br>
					<br>
					The first project is the development of a major deepwater 
					bulk jetty to support the bulk shipping needs of Brazilian 
					mining conglomerate Vale, which is building a huge iron ore 
					pelletising and distribution centre at the industrial port. 
					The jetty is being built in a JV by Italy-based 
					international engineering contractor Saipem and leading 
					Indian infrastructure firm AFCONS.<br>
					<br>
					The second project is the development of a General Dry Bulk 
					and Aggregates Terminal. “We are quite excited by the 
					deepwater jetty as there are only nine such jetties around 
					the world and this is the first in the region. In addition, 
					the investment in the Dry Bulk Terminal will add to the 
					versatility of the Port of Sohar,” comments Jan Meijer, CEO, 
					Sohar Industrial Port Company (SIPC), which oversees Port of 
					Sohar and the Sohar Freezone. With these facilities, the 
					Port of Sohar will have the capacity to handle bulk imports 
					and exports of aggregates, as well as commodities like 
					limestone, coal, cement and chromite, to name a few.<br>
					<br>
					“We have all the ingredients to make Oman the logistics hub 
					for the whole region and we should all work hard to make it 
					a reality in the future because it will provide a lot of 
					employment opportunities for the Omani people in the various 
					logistic sectors,” Meijer says.<br>
					<br>
					</font><b>
					<font size="2" face="Verdana, Arial, Helvetica, sans-serif" color="#000080">
					Excellent connectivity</font></b><font size="1" face="Verdana, Arial, Helvetica, sans-serif"><br>
					<br>
					With excellent road infrastructure and the rail-sea-air 
					links in place, Oman is well placed to provide unprecedented 
					connectivity to the world thus establishing the case for 
					Oman to become the first choice for investors in the 
					logistics and transport services business, pointsout Nahari.<br>
					Oman’s railway system is part of a strategy to connect all 
					the GCC countries with a railway network. Experts think that 
					this is going to be very important for business not only in 
					Sohar but also the whole of Oman. The railway is going to be 
					useful in supplying minerals and transporting finished goods 
					as well as the containers.<br>
					<br>
					Says Jamal T Aziz, CEO, Sohar Freezone, “The idea really for 
					us is not to separate the ports from the airports and other 
					modes of transport because they all have to work together. 
					When you have a location like Oman outside the Strait of 
					Hormuz, then you have to look at how to supply the goods to 
					those countries, which you are not entering. And for that 
					the connectivity is important. Sohar for Oman is what 
					Rotterdam is for Europe, a gateway. In Sohar’s case, you can 
					make short voyages to Iran, Pakistan, India and other GCC 
					countries due to the logistics distance.”<br>
					<br>
					Aziz feels the integration of these transport systems is 
					important because investors in various fields would like to 
					see how they can ship or airfreight some of their goods from 
					the area where they are situated in this case, the ports or 
					the free zones. “So, because they (investors) are interested 
					in the airports, we are interested in the airports and we 
					want to see that the airports are integrated with the port 
					and the free zones,” observes Aziz.<br>
					<br>
					“In Sohar, we don’t like to say Port of Sohar, we like to 
					say Gateway Sohar which consists of the port, industrial 
					zones, free zone, airport, railways and expressways and 
					those really are our strengths,” Aziz adds. <br>
					<br>
					</font><b>
					<font size="2" face="Verdana, Arial, Helvetica, sans-serif" color="#000080">
					Free Zones</font></b><font size="1" face="Verdana, Arial, Helvetica, sans-serif"><br>
					<br>
					Free zones are only extensions to really large transport 
					infrastructure like ports and airports. Free zones near big 
					transport systems like ports, airports and railways can help 
					you to do business in an environment more conducive to 
					investment and at the same time be connected to these very 
					large transports systems. In the free zone, if you want to 
					do business whether setting up a distribution hub or a 
					manufacturing unit or other activity, there you can have 100 
					per cent ownership.<br>
					<br>
					Sohar Freezone is fast becoming a reality with the 
					authorities signing the first civil contract with the UAE-based 
					Al Naboodah Contracting for developing the area. The first 
					of the three phase development includes an area of 500 
					hectares, out of the total 4,500 hectares envisaged in the 
					master plan. The free zone is expected to attract huge 
					foreign direct investment (FDI), besides boosting the 
					industrial development of Batinah region.</font><p align="center" style="margin: 3px 10px" dir="ltr">
					<font size="1" face="Verdana, Arial, Helvetica, sans-serif"><br>
					<br>
					<img border="0" src="images/jan_cover3.jpg" width="400" height="328"><br>
&nbsp;</font><p align="justify" style="margin: 3px 10px" dir="ltr">
					<font size="1" face="Verdana, Arial, Helvetica, sans-serif">Neelima Vyas, chief operating officer of Sohar Freezone, 
					informs that six companies from Gulf Co-operation Council (GCC), 
					Indian sub-continent and Europe have already signed up for 
					setting up industries within the free zone. These companies 
					will set up downstream industries including oil and gas 
					enclave, a steel cluster, logistics firms, container repair 
					facilities and distribution firms. The total investment for 
					developing the free zone in the region is estimated at 
					RO20-25mn. <br>
					The first company within the Sohar Freezone is expected to 
					start operation by third or fourth quarter of next year. The 
					container, general cargo and liquid terminals and the 
					proposed bulk dry dock terminal dedicated for Vale are all 
					attractions for companies planning to set up industries 
					within the free zone.<br>
					<br>
					Down south, the Salalah Free Zone (SFZ), adjacent to Port of 
					Salalah is another area of attraction for investors and 
					businessmen. SFZ, established in June 2006, is being 
					developed in multiple phases over an area of 2,000 hectares. 
					The zone offers a mix of industrial, manufacturing, 
					warehousing, logistics and distribution. Phase One, which is 
					now being developed, will provide 200 hectares of 
					distribution, logistics, freight forwarding and 
					manufacturing facilities. The necessary infrastructure 
					services will include a road network with power, water and 
					other services. <br>
					<br>
					SFZ has attracted eight projects with total investment of 
					over $2bn. These include the $300mn Octal Petrochemicals for 
					manufacturing PET; the Salalah Methanol project, with an 
					investment of around $900mn; Dunes Oman, a company backed by 
					Brakes India to produce ductile iron and grey iron castings 
					and the fourth is Dhofar Nutraceuticals, which will target 
					the global market for food supplements and energy drinks.<br>
					The free zones in Oman provide all of the incentives 
					typically offered in special economic zones including 100 
					per cent ownership by foreign investors, exemption from 
					customs duties on raw materials and finished products, no 
					income tax, no restrictions on the transfer and repatriation 
					of income and long term leases.<br>
					<br>
					Omanisation levels in the free zones is 15 and 10 per cent 
					in Sohar and Salalah respectively. In the case of Sohar 
					Freezone, tax breaks are upto 25 years.<br>
					<br>
					</font><b>
					<font size="2" face="Verdana, Arial, Helvetica, sans-serif" color="#000080">
					Duqm development</font></b><font size="1" face="Verdana, Arial, Helvetica, sans-serif"><br>
					<br>
					The most astounding ports and special economic zone 
					development in the country is Duqm development. If all goes 
					well according to the master plan, Duqm will be a force to 
					reckon with in the entire Gulf region in the days to come. 
					As per the master plan, the developments include a crude oil 
					export terminal and a strategic storage facility, a free 
					trade zone and a downstream industrial area in addition to 
					an airport, port, dry dock, commercial and residential areas 
					and plans for tourism development.<br>
					<br>
					Thanks to its proximity to the busy regional sea-lanes 
					traversing Oman’s coastal waters, Duqm, about 700 kms from 
					Muscat, is being conceived as a main maritime gateway that 
					will serve an ambitious industrial and commercial hub. Apart 
					from the strategic location of Duqm, the friendly climate of 
					the area adds to its advantages. Positioned as it is on the 
					Gulf of Oman, with the Strait of Hormuz and the Arabian Sea 
					at its north-eastern end, and with its long coastline 
					running south along the Arabian Sea, the government believes 
					Duqm is strategically the most preferred location for this 
					port.<br>
					<br>
					“We think that the future of ports is Duqm as it will boost 
					the region of Al Wusta for the economy and for the society 
					at large. There is a good potential,” says HE Harthy 
					describing the Duqm development. The plan for Duqm is to 
					position it as the region’s leading ship-repair centre by 
					constructing a massive dry dock and maintenance facility. 
					Khalid Ahmed Al Salmi, deputy CEO, Oman Drydock Company 
					says, “we have signed a deal with South Korea’s Daewoo 
					Shipbuilding and Maritime Engineering Company to manage and 
					operate the dry dock for a period of 10 years. We are 
					building two dry docks and a floating dock. Even though all 
					the facilities are not there, it is a virgin area and an 
					attraction for investors because you can build anything over 
					there and it will grow.”<br>
					<br>
					Besides the dry dock, the government tied up with Belgium’s 
					Port of Antwerp in a 50:50 JV to manage the port at Duqm, 
					which is currently being construction by a consortium of Jan 
					De Nul, Consolidated Contractors Company and Swzai Turkes 
					Feyzi Akkaya (STFA). Experts have said that this tie-up is a 
					shot in the arm for the government’s efforts to develop a 
					world scale industrial and maritime hub on the Al Wusta 
					coast. <br>
					<br>
					</font><b>
					<font size="2" face="Verdana, Arial, Helvetica, sans-serif" color="#000080">
					Shipping</font></b><font size="1" face="Verdana, Arial, Helvetica, sans-serif"><br>
					<br>
					Along side the ports and free zones, the government is also 
					augmenting the shipping infrastructure. The maiden voyage of 
					container carrier ‘Fanja’, was launched from Port Sultan 
					Qaboos to the ports of the Mediterranean Sea and Red Sea in 
					mid-December, 2010. The ship belongs to Oman Container 
					Company, which is a part of the Oman Shipping Company (OSC) 
					incorporated in 2003. OSC has a 42-ship fleet including very 
					large carriers for iron ore and bulk LNG carriers.<br>
					<br>
					The International Maritime College, Oman (IMCO) will provide 
					the shipping sector with graduates in various specialties. 
					The establishment of Oman Container company and the maritime 
					college will complete the Sultanate’s shipping industry 
					infrastructure.<br>
					With so many developments, Oman is steaming ahead on the 
					growth route. And we can only say Godspeed.<br>
