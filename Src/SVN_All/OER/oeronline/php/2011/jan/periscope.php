					<b><font face="Verdana" size="1" color="#C00000">&nbsp;</font><br>
					<font face="Verdana" size="4" color="#C00000">Making money 
					in Asian equities<br>
                    </font></b> 
					<font face="Verdana" size="1"><br>
					</font><b><font face="Verdana" size="2">Country allocation 
					is essential while investing across Asia in 2011, because 
					Asian stock markets are not expected to move up in unison</font></b><font face="Verdana" size="1"><br>
                    <br>
                    <br>
                    Asia 
					has the highest economic growth, most stellar sovereign 
					balance sheet and impressive savings ratios on the planet. 
					However, Asian stock markets will not all move up in unison 
					in 2011. Country allocation is essential while investing 
					across Asia in this year.<br>
					<br>
					Geopolitical shocks on the Yellow Sea have created a 
					compelling valuation for South Korean shares, which trade at 
					a forward valuation metric of only 9.5 times earnings. The 
					KOSPI index is the third cheapest stock market in Asia after 
					Vietnam and Pakistan, thanks to the military aggression from 
					North Korea.<br>
					<br>
					Yet South Korean companies, which include global icons such 
					as Samsung Electronics, Hyundai Motors and Posco Steel, can 
					well rise 25 per cent due to an improving global economy, 
					better Silicon Valley tech spending, low interest rates and 
					huge capital inflows as the won has the potential to 
					appreciate to 1000 against the dollar from its current 1150 
					exchange rate. Risks to the KOSPI include a risk aversion 
					global sell offs (Seoul’s free float is dominated by foreign 
					money), escalation of tensions with North Korea and the very 
					real possibility of a property crash. I expect South Korea’s 
					KOSPI index will range between 1800 and 2500 in 2011, making 
					the Korea Fund (KF) on the NYSE one of my favourite buys on 
					any market selloff.<br>
					<br>
					</font><font face="Verdana"><b>
					<font color="#000080" size="2">Chinese inflation</font></b></font><font face="Verdana" size="1">
					<br>
					<br>
					Chinese shares have lost 15 per cent since the October CPI 
					shocked the Shanghai and Hong Kong financial markets. 
					Inflation at 4.4 per cent is unacceptable to the Chinese 
					Politburo because it generates social unrest. This is the 
					reason the Chinese central bank has tightened monetary 
					policy while the government has imposed price controls on 
					diesel, vegetables and cooking oil and launched a crackdown 
					on commodities hoarders/speculators.<br>
					Chinese inflation will only peak at 6-7 per cent in the next 
					six months so it is entirely possible that the People’s Bank 
					of China (PBOC) will raise interest rates by at least 
					another 100-150 basis points. Inflation will hit Chinese 
					valuations. So, I expect downside risk on the Shanghai 
					Composite A share index, which can fall to 2500 by next 
					summer as the Chinese central bank engineers tight money. 
					Investors will only increase exposure to Chinese equities 
					once they are convinced that the inflation rate has peaked 
					and the central bank will reflate the Shanghai money 
					markets. Historically, inflation has been an obsessive 
					policy focus for Chinese governments due to painful memories 
					of the Tiananmen Square massacre, which was preceded by an 
					inflation shock.<br>
					<br>
					Moreover, Chinese shares can lose 20 per cent during periods 
					of anti-inflation monetary tightening. However, Chinese 
					large caps listed in Hong Kong (H shares) are cheap and have 
					largely priced in a hard landing. MSCI China is not very 
					expensive at 15 times earnings. Shanghai can bottom at 2500 
					and trade in a 2500-3400 range in 2011, with a bull run 
					possible only after the summer.<br>
					<br>
					</font><font color="#000080" size="2" face="Verdana"><b>
					Mired in scam </b></font><font face="Verdana" size="1"><br>
					<br>
					It is no longer possible to be optimistic about Indian 
					equities at current levels. The ruling Congress is mired in 
					the $40bn 2G telecom scandal, the most explosive political 
					scandal in India since Bofors in the 1980s. India trades 
					above three times book value and 20 times earnings, making 
					it the most expensive major emerging market in the world. 
					The bribery/loans scandal could well devastate the valuation 
					of India’s public sector banks and insurers. Domestic mutual 
					funds have sold $6bn in shares while foreign investors have 
					invested $26bn in Dalal Street.<br>
					<br>
					Stratospheric valuation bubbles cannot co-exist with foreign 
					selling, a property crash, domestic mutual funds cutting 
					exposure, a coalition split between Congress and Tamilnadu’s 
					political elite, RBI rate hikes scandals in 
					microfinance/housing finance/corporate loan funding and the 
					resurgence of global risk aversion (Greece, Ireland, North 
					Korea etc.). I believe the risk of a swift, substantial bear 
					market in India is all too real and the Sensex can trade 
					down to 16000.<br>
					<br>
					However, the Indian equities correction can create 
					compelling long/short opportunities, such as buying Bharti 
					Airtel while selling Reliance Communications. An Indian bank 
					that could well prove profitable is IndusInd Bank, an 
					admittedly small bank that is on the eve of a major 
					restructuring in its 200 branch network. This is a bank that 
					can well deliver 35-40 per cent EPS growth in 2011 as it 
					triples its branch network, loan volumes and fee income. 
					IndusInd Bank actually rose while the Indian banking sector 
					witnessed its major correction since September. However, I 
					think HDFC Bank and ICICI shares are still expensive and 
					face significant downside risk.<br>
					<br>
					</font><font color="#000080" size="2" face="Verdana"><b>
					Ideal hedge </b></font><font face="Verdana" size="1"><br>
					<br>
					Singapore has been one of the world’s most successful 
					economic success stories since the crisis, with GDP growth 
					of 15 per cent as it emerged as Southeast Asia’s high tech, 
					finance, aviation and logistic hub. As a small, open economy 
					where exports are more than twice GDP, the Singapore dollar 
					has risen from 1.65 to 1.30 against the US dollar in the 
					last five years, making it an ideal hedge for GCC 
					investors/savers exposed to the depreciation of the 
					greenback. Singapore has world class companies like SIA, 
					Singtel, DBS, Capital Land, Mapletree REIT. I expect the 
					Straits Times Index to rise another 25 per cent to 3800 – 
					4000 in 2011. <br>
