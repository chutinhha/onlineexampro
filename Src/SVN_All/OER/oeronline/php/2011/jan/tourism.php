                  <p align="justify" style="margin: 3px 10px"> 
					<b><font face="Verdana" size="1" color="#C00000">&nbsp;</font><br>
					<font face="Verdana" size="4" color="#C00000">Ensuring fair 
					returns<br>
                    </font></b> 
					<font face="Verdana" size="1"><br>
					</font><font face="Verdana" size="2"><b>Having completed its 
					major project, the Asian Beach Games site, Omran is in an 
					upbeat mood and will continue to play a major role in 
					strategic tourism-related projects in the country, says </b>
					<i>Wael 
					Al Lawati</i><b>, CEO, Omran, in a interview with </b><i>Visvas 
					Paul D Karra</i></font><font face="Verdana" size="1"><br>
                    <br>
                    <img border="0" src="images/jan_tourism1.jpg" align="left" hspace="12" width="150" height="179" style="border: 2px solid #C0C0C0">The 
					Sultanate of Oman has witnessed a fantastic journey over the 
					last 40 years, under the enlightened leadership of His 
					Majesty Sultan Qaboos bin Said. Under His Majesty’s wise 
					leadership, the country has witnessed rapid economic 
					developments and one of the key components of this 
					development is the farsighted policy of diversifying the 
					national economy from oil to non-oil sectors. In this 
					regard, a major emphasis has been laid on the tourism sector 
					as one of the promising growth areas.<br>
					<br>
					“The tourism sector has witnessed massive strides in the 
					development of infrastructure to create a tourism industry. 
					It is incredible to think that in only 40 years, the nation 
					has become one of the world’s most attractive tourism 
					destinations. As we look back with pride, we should also 
					look forward with optimism. The policies and investments in 
					place today point to a bright future, one in which our 
					diversified economy offers employment and wealth to future 
					generations,” says Wael Al Lawati, CEO, Omran.<br>
					<br>
					As the government’s investment arm in the tourism sector, 
					Omran has been in the forefront of encouraging 
					tourism-related investments in the country, having been 
					established by the Government to create memorable 
					destinations and experiences, which deliver long-term 
					benefits to local communities and the national economy, 
					working in harmony with Oman’s environment and natural 
					resources.<br>
					<br>
					As a development and management company, Omran’s projects 
					include a portfolio worth almost RO4bn. This includes major 
					projects such as the Asian Beach Games village and the Oman 
					Convention and Exhibition Centre, and hotel properties 
					throughout the country.<br>
					<br>
					In addition, Omran is involved in more than 20 joint 
					ventures of different types, with a wide range of partners. 
					Its international partners in this area include Orascom, 
					Saraya, Qatari Diar and Daewoo. Increasingly, Omran is 
					becoming the first stop for international investors in the 
					tourism sector and they are pleased that they have been able 
					to act as the catalyst behind so many projects.<br>
					<br>
                    <br>
                    <b>Joint ventures<br>
					</b><br>
                    According to Lawati, Omran has a range of projects underway 
					with international partners. For example, its Muriya joint 
					venture, with Egypt’s Orascom Hotels and Development, has 
					three major projects under development. The two large 
					master-planned residential and tourism developments, Jebel 
					As Sifah and Salalah, have seen significant construction 
					progress over the last year and will see handovers in the 
					first quarter of 2011. The third, As Sodah Island, a 
					development of a boutique hotel and eight luxury villas, has 
					also began construction and phase one is expected to be 
					complete by the end of 2012.<br>
					<br>
					Omran has also seen good progress on the Ad Duqm Frontier 
					Town project in which it holds a 50 per cent stake. This 
					will see the development of 55 hectares of land in total, in 
					the Ad Duqm new town. The first phase will consist of 344 
					units – 250 one-bedroom apartments, 89 three-bedroom villas 
					and five four-bedroom villas.<br>
					<br>
                    <br>
                    <b>Benefits to economy<br>
					</b><br>
                    Talking about the benefits accruing to the national economy 
					as well as to the society through Omran’s activities, Lawati 
					opines that tourism is clearly and critically important for 
					future economic growth of the country. Today, tourism 
					represents 2.9 per cent of GDP, equivalent to around 
					RO1.07bn. By 2019, this is estimated to reach 9.9 per cent 
					of GDP, employing 133,000 workers.<br>
					“As the leading developer in this sector, and as a company 
					established by the government, we play a role in strategic 
					projects across the nation, ensuring the benefits of this 
					economic growth reach all areas of the country,” Lawati 
					says.<b><br>
					<br>
					Hospitality management<br>
					</b><br>
                    Omran now oversees the management of a growing portfolio of 
					hotels, resorts and tourism assets. The company now has nine 
					properties under various levels of supervision and 
					management. Omran aims to bring consistent quality, service 
					and management levels to all of the properties, as well as 
					committing investments to ensure that the country’s hotel 
					stocks match up to international standards. <br>
					<br>
					“Our role is to ensure each property delivers a fair return 
					and that together, they contribute to the long-term success 
					of the tourism sector in Oman,” maintains Lawati while 
					adding that their portfolio includes a number of the 
					government’s hotels. Over the years, the Government had 
					built up a diverse portfolio of hotel properties, under the 
					management of different ministries. It was felt sensible to 
					bring them into unified ownership and management, under a 
					body, which has expertise in the tourism sector.<br>
					<br>
					Omran has therefore taken over ownership of the 
					InterContinental Muscat, the Swiss Belhotel on Masirah 
					Island, the Golden Tulip Khasab and the Golden Tulip Dibba. 
					In addition, it has taken on supervision role for the Al 
					Bustan InterContinental Muscat, the Oman Dive Centre and the 
					Ad Duqum Tourist Guest House; and the direct management of 
					the Ras Al Jinz eco-tourism centre.<br>
					<br>
					While developing the tourism sector of the Sultanate, the 
					government has laid special emphasis on responsible and 
					sustainable tourism. Elaborating on this, Wael says, “we 
					have set ourselves a clear target of putting responsible 
					tourism as one of our objectives for the future. That means 
					a commitment to the environment, a commitment to the local 
					communities and a commitment to the unique national 
					heritage.<br>
					Oman has truly been blessed with its natural resources – 
					flora and fauna, beautiful landscape and a rich history. 
					Therefore, the country should welcome visitors who wish to 
					experience these things but it should be careful to nurture 
					and protect them at all times, feels Lawati.<br>
					<br>
