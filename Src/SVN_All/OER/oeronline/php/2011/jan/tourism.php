                  <p align="justify" style="margin: 3px 10px"> 
					<font size="1"><font face="Verdana"><b>Future plans</b><br>
					<br>
					Omran as an organisation has grown phenomenally quickly 
					since it was established in 2005. Lawati says that with a 
					portfolio of projects under development worth more than 
					RO4bn, plus another 20 joint ventures, plus nine hotel 
					properties under management, it is a tremendous position to 
					be in, in such a short time.<br>
					<br>
					What does the future hold? Even more projects, in each of 
					those areas. In December, Omran completed its first major 
					development, the Asian Beach Games site, and the 
					organisation is moving forward on many others. Through next 
					year and beyond, Omran will see more completions and 
					therefore new properties coming under its management. “But 
					we will never stop our appetite for new projects which will 
					bring long-term benefit to Oman’s tourism sector and to the 
					economy as a whole,” Lawati adds.</font><br>
