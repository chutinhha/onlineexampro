						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">
						The Porsche DRIFT<br>
&nbsp;</font></b><p align="center" style="margin: 10px; ">
				<font face="Verdana" size="1">
				<img border="0" src="images/autotalk-july-1.jpg" style="border: 1px solid #808080" class="style4" width="450" height="335"></font><p align="justify" style="margin: 10px; ">
				<font face="Verdana" size="1" class="style11" style="font-size: 11px">
				<b>Open roads bending into steep curves along the magnificent 
				Alps make for the perfect setting to drive a Porsche, Akshay 
				Bhatnagar reports from Munich</b></font><p align="justify" style="margin: 10px; ">
				<font face="Verdana" size="1" class="style11" style="font-size: 11px">
				<br>
				Ask any sports car lover about his favourite car and in all 
				likelihood, the answer will be a Porsche. Most of the car 
				enthusiasts make it a point to have a Porsche among their prized 
				possessions. So when Porsche Travel Club (PTC) invited Oman 
				Economic Review to test drive their beautiful mid-engine Boxster 
				and Cayman models in serene settings of Alps mountain range, it 
				was a dream come true for yours truly. The invitation entailed a 
				drive from Munich (Germany) to Kitzbuehel (Austria) and back 
				over a period of two days. I was the first one to drive in my 
				team. After driving through the city roads for a while, we 
				reached the winding country roads towards Lake Chiemsee. The 
				Cayman R communicates its willingness to perform to the driver 
				right from the start.<br>
				<br>
				</font><font face="Verdana" size="1" class="style11">
				<font color="#000080" size="2"><b>Powertrain</b></font></font><font face="Verdana" size="1" class="style11" style="font-size: 11px"><br>
				<br>
				Given its purposeful mid-engine layout, the Cayman R has a power 
				output of 330hp and reaches 282 km/h top speed. It looks and 
				feels like a proper race car thanks to the 19-inch lightweight 
				alloy wheels, sports bucket seats, a lowered body and a retuned 
				sports suspension. Equipped with the seven-speed PDK, the 
				acceleration time from 0 to 100 km per hour is 4.9 seconds only.<br>
				<br>
				By afternoon, we reached Lake Chiemsee – a freshwater lake 
				between Rosenheim, Germany, and Salzburg, Austria. Post lunch, 
				we continued our drive through the foothills of Alps in a new 
				car, Cayman S – another great offering in the Cayman range of 
				Porsche. It boasts a brand-new six-cylinder boxer engine, 
				displacing 3.4ltrs and again setting a new benchmark in terms of 
				both fuel economy and performance thanks to direct fuel 
				injection. With a maximum output of 320bhp and impressive 
				acceleration to 100km per hour in 5.1 seconds, Cayman S was a 
				sheer delight to drive. With no speed cameras around, you could 
				easily achieve a top speed of 277km per hour.<br>
				<br>
				By evening, the caravan reached the destination – Royal Spa 
				Kitzbühel Hotel in Austria. The next day, it was time for the 
				journey back to Munich. We got behind the wheel again. This time 
				we got our hands on the Boxster. The highlight of the new 
				generation Boxster is the six-cylinder boxer engine displacing 
				2.9ltrs in the Boxster. This engine belongs to the same family 
				as the efficient power units introduced recently in the 911 
				model series.<br>
				<br>
				It comes with its own unique profile, standing out clearly from 
				the Cayman. Seen from the front, the extra-large air intakes 
				characterise the superior look of the model and accentuates its 
				superior performance. Two of the crossbars integrated on either 
				side in the outer air intake scoops are finished in body colour 
				on the Boxster, adding a new graphic effect at the front to make 
				Porsche’s new roadster quite unmistakable at very first sight.<br>
				For the last leg of our journey, we had the stylish Boxster S to 
				drive. The combination of wide track with wide wheels and tyres, 
				together with the mid-engine, allows a very dynamic style of 
				motoring with enhanced driving stability and lateral 
				acceleration in Boxster S. Body roll and dive are reduced to a 
				minimum, with the long wheelbase of the Roadster ensuring 
				additional stability when driving in a straight line.<br>
				<br>
				I savoured every kilometre of the return journey. As we inched 
				closer to Munich, our tour guide generously allowed me to test 
				the Boxster S to the hilt on a stretch of the expressway. I 
				touched the top speed of 260km per hour to mark a fitting finale 
				to an unforgettable drive!<br>
				<br>
				Porsche Travel Club offers a wide range of paid driving 
				programmes throughout the year in various locations in Europe 
				and other places. If you wish to participate in them, visit
				<a href="http://www.porsche.com/travelclub">
				www.porsche.com/travelclub</a>&nbsp; for more information.</font></td>
