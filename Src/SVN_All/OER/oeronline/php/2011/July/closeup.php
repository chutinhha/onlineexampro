						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">
						Reliable energy leader<br>
&nbsp;</font></b><p align="justify" class="style13">
				<strong><span class="style14">The six-nation Gulf Cooperation 
				Council (GCC) leads the world in exports of crude oil and LNG<br>
				<br>
				<font color="#808080"><i>By Dr Jasim Husain Ali</i></font><br>
&nbsp;</strong></span><p align="center" class="style13">
				<strong><span class="style14"><br>
				</span></strong><br>
				<img border="0" src="images/closeup-july-1.jpg" style="border: 1px solid #FFFFFF; " width="450" height="299"><br>
&nbsp;<p align="justify" class="style13">
				The six-nation Gulf Cooperation Council (GCC) is the global 
				leader in energy resources, a fact that should provide comfort 
				to many stakeholders. Relying on its spare capacity, Saudi 
				Arabia has traditionally shown the readiness to increase crude 
				output in the event of any foreseeable drop in production due to 
				diverse factors including political turmoil. A case in point is 
				the current production problems facing Opec member Libya, a 
				country boasting an output of 1.5mn barrels per day (bpd).<br>
				<br>
				<font color="#000080" size="2"><b>Proven reserves<br>
				</b></font><br>
				To be sure, Saudi Arabia falls behind Russia in terms of 
				production of oil, but the Kingdom stands out as the largest oil 
				exporter. According to the recently published BP Statistical 
				Review of World Energy, Russia and Saudi Arabia accounted for 
				12.9 per cent and 12 per cent of global oil output in 2010, 
				respectively. Yet, unlike Russia, the Kingdom exports a 
				substantial portion of its output. Likewise, other GCC countries 
				too stand out for their export capabilities, notably the UAE and 
				Kuwait, which boast 3.3 per cent and 3.1 per cent of global oil 
				output, respectively. Qatar and Oman account for 1.7 per cent 
				and one per cent of worldwide oil output, respectively.<br>
				<br>
				Bahrain too is sparing no efforts to enhance oil output capacity 
				thanks to a 20-year production sharing agreement with Occidental 
				Holding of the US and Mubadala of the UAE. Signed in 2009, the 
				deal aims to enhance output of onshore fields from 33,000 bpd to 
				100,000 bpd in a span of eight years.<br>
				<br>
				Still, Bahrain’s main crude source is the offshore exploration 
				zone of the Abu Saafa Field. Saudi Arabia and Bahrain equally 
				share the field’s output, which currently stands at 300,000 bpd. 
				Saudi Arabia manages the field and transfers the proceeds to 
				Bahrain after deducting expenses. Saudi Arabia alone sits on 19 
				per cent of oil reserves, second to none worldwide.<br>
				Kuwait and the UAE possess 7.3 per cent and 7.1 per cent of oil 
				reserves, respectively.In reality, GCC countries account for 77 
				per cent of Opec’s share of proven oil reserves. Opec’s strength 
				reflects its members’ reserves and steady production capacity.<br>
				<br>
				Conversely, Opec supplied around 42 per cent of worldwide oil 
				output in 2010. Other than oil, GCC states are also noted for 
				their gas reserves. According to BP’s publication, Qatar’s 
				global share of natural gas reserves stands at 13.5 per cent. 
				Only Russia and Iran possess more gas reserves than Qatar — 23.9 
				per cent and 15.8 per cent, respectively.<br>
				<br>
				More importantly, Qatar leads the world in the production of 
				liquefied natural gas (LNG). Only recently the country 
				celebrated reaching LNG output capacity of 77mn tonnes per 
				annum, second to none globally. As such Saudi Arabia and Qatar 
				lead the world in exports of crude oil and LNG respectively.<br>
				<br>
				<b><font color="#000080" size="2">Innovative agreements <br>
				</font></b><br>
				In addition, innovative production sharing agreements with 
				international oil firms allowed Qatar to boost its oil output 
				from 760,000 bpd in 2010 to 1.5 mn bpd in 2010. Also, the 
				extraordinary initiatives helped Qatar boost its oil reserves 
				from 16 bpd to 26 bpd in the same time span.<br>
				<br>
				By and large, GCC countries are contributing towards supporting 
				and maintaining contemporary quality of life by being reliable 
				energy sources for planes, cars, and machinery. The adage about 
				a friend in need being a friend indeed neatly fits the GCC’s 
				working relationship with global consumers of petroleum 
				products. At the same time, GCC countries need to export 
				petroleum for the survival and prosperity of their economies. 
				Hence, it is a give and take situation. <br>
