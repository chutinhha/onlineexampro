						<p align="justify" style="margin: 3px 10px">
						<span class="style27"><strong>ON A SOLID FOOTING</strong></span><p class="style15">
				&nbsp;<p align="justify" class="style13">
				<b><span style="font-size: 9pt">
				<img border="0" src="images/finance1-may2011.jpg" width="150" height="171" align="left" style="border: 1px solid #808080"></span></b><p align="justify" class="style13">
				<b><span style="font-size: 9pt">Over the last two decades, 
				Muscat Finance Company has carved a niche for itself in the 
				non-banking finance sector and has contributed to the 
				development of the Sultanate through its extensive branch 
				network, tells Ajit Engineer, CEO, to <font color="#808080"><i>
				Muhammed Nafie</i></font></span><i></i></b><br>
				<br>
				Established in 1987, Muscat Finance Co (MFC) is promoted by 
				prominent business houses, vehicular distributors, banks and 
				insurance companies in the country. During the 23 years of its 
				operation, the company has pioneered the concepts of 
				hire-purchase financing, equipment leasing, debt factoring, 
				working capital financing and consumer durable financing in the 
				Sultanate. Over the two decades, it has established an enviable 
				reputation, maintained its track record of uninterrupted profits 
				and dividends and set records in terms of balance sheet build-up 
				and profitability.<br>
				“We have been here for 23 years going through almost six to 
				seven business cycles. We have carved a niche in an industry 
				that is extremely competitive with all six fairly capitalised 
				non-banking finance companies (NBFCs) aggressively seeking a 
				share of the market,” says Ajit Engineer, CEO, MFC.<br>
				<br>
				Engineer, who joined the company in 1992 as finance manager and 
				served in various capacities to become a valuable part of the 
				company’s remarkable success story, recalls how MFC has played a 
				significant role in the development of the country and its 
				people. “People who have been with us since 1980s continue to 
				grow with us. Many people who have launched small businesses 
				have grown with MFC. There are a lot of small and medium 
				business houses who are doing extremely well today. A number of 
				companies associated with us are prospering very well.” <br>
				<br>
				Though headquartered in Muscat, MFC has an extensive branch 
				network across the country which enables it to employ young 
				Omani nationals from the interior as well as provide a close 
				inter-face with local customers and business associates. Six 
				branch offices at Barka, Sohar, Sur, Salalah, Ibri and Nizwa 
				coupled with well-trained and skilled employees enable the 
				company to serve customers in the interior and offer excellent 
				dealer support and credit follow-up.<br>
				<br>
				<font color="#FF0000"><b>Conservative and cautious<br>
				</b></font><br>
				MFC has continued its cautious approach in asset build-up and 
				its balance sheet has grown from RO69mn in 2008 to RO82mn in 
				2010. “The performance has been extremely satisfactory given the 
				difficult period experienced by majority of the business during 
				the last two years. Pursuing a conservative approach, we have 
				planned to grow both the assets and profits moderately in the 
				year 2011,” says Engineer. He exudes confidence saying that the 
				company is looking to achieve its objectives by implementing a 
				strategy comprising of an improved customer service, dealer and 
				client responsive marketing, selective recruitment of additional 
				service staff and various special schemes and promotions.<br>
				<br>
				Elaborating on the leasing industry in the Sultanate, Engineer 
				reveals that the industry’s asset base comprises of 
				approximately RO530mn amongst the six NBFCs. Apart from six 
				licensed financial institutions, there are local commercial 
				banks who offer personal finance products and SME financing 
				schemes which are complimentary to ours. They, in addition, 
				constitute the major providers of funds to the NBFCs. <br>
				<br>
				“The suppliers of equipment and automobiles act as distribution 
				channels for finance. Some of them also offer in-house finance, 
				and are, both, a source of business referral and significant 
				influencers of customer behaviour. With the six NBFCs, 
				commercial banks and vehicle distributors, the market is 
				extremely competitive. MFC has successfully withstood this 
				competition and delivered superior results over the last 23 
				years,” avers Engineer. <br>
				<br>
				<b><font color="#FF0000">Brighter prospects </font><br>
				</b><br>
				He says that since Oman is a growing economy there is 
				substantial requirement for infrastructure development and the 
				need of capital requirements which are partially fulfilled by 
				banks and nonbanking companies. According to him, a plethora of 
				welfare measures recently announced by the government which 
				include creation of 50,000 additional jobs for Omani nationals 
				and various infrastructure spending constitute twin benefits for 
				the NBFCs. <br>
				“Everybody should take this in a positive manner. I envisage 
				huge opportunities and brighter prospects for growth in the long 
				run. The coming years could see an upswing in the economic 
				activity as these measures could give more impetus for growth. 
				All these welfare measures and infrastructure spending will help 
				increase the purchasing power of people and this will create 
				demand for equipment and vehicles translating into great 
				opportunities for NBFCs,” says Engineer. <br>
				<br>
				<b><font color="#FF0000">Diversified services <br>
				</font></b><br>
				MFC introduced ‘Debt Factoring’ services to support the SME 
				sector. ‘MFC Debt Factoring’ is a post sales financial mechanism 
				which has helped many SMEs to bolster their cash flow and grow 
				their business at will. Debt Factoring comprises a portfolio of 
				services such as instant financing up to 70-75 per cent on the 
				credit invoices, sales ledger maintenance and debt collection 
				services. In 2002, Muscat Finance launched ‘Working capital’ 
				facilities to cater to the growing pre-sale working capital 
				requirements of SMEs, who provide supplies/services to 
				corporates and Government Institutions. “MFC ‘Working capital’ 
				scheme is an ideal financial mechanism for SMEs and midsize 
				corporates to execute their work orders on time. It includes 
				non-fund based guarantees, performance bonds etc., and working 
				capital term loans for smooth execution of the projects. The 
				demand for these products has been growing consistently and the 
				business contribution from these products is over RO 10mn,” 
				describes Engineer. <br>
				<br>
				MFC’s Business Continuity Plan (BCP) comprises procedures to be 
				carried out in the event of a major incident affecting its 
				operations. The advantages of BCP are to minimise the impact 
				that an incident can have on the on-going business services 
				provided by the company and allow for the efficient resumption 
				of critical business processes/functions in the wake of any 
				incident rendering the head office facility inaccessible. It 
				also provides guidelines on actions to be taken in achieving the 
				continuity of operations. These aspects are comprehensively 
				listed and consolidated into a document so that the information 
				is easy to maintain and implemented by users. MFC has placed the 
				BCP site at its Barka branch with adequate back-up equipment to 
				resume services in case of any break-down at the headquarters.<br>
				<br>
				MFC also has a Disaster Recovery Site (DRS) facility, a 
				secondary site where data is replicated simultaneously through 
				leased lines, to run the operations in case of a major 
				disruption in the primary site located in MFC head office, thus 
				providing a pre-planned response to catastrophic events. “MFC 
				has located the DRS at its Nizwa branch with a powerful server 
				from where the normal activities can be resumed. <br>
				<br>
				The advantages of this site is to provide an organised and 
				consolidated approach to manage IT response and recovery 
				activities following an unplanned incident or interruption, thus 
				avoiding confusion and reducing exposure to business 
				interruption. It also helps to recover the applications and 
				services supported by IT department in a timely manner,” 
				explains Engineer. <br>
				<br>
				During the last two years, apart from introducing BCP and DRS, 
				the company has successfully completed a Rights Equity Issue of 
				RO4mn to increase the capital in compliance of regulatory 
				requirements. Some of the important measures it has recently 
				implemented include an employee grading exercise and a 
				customer-relationship management software. Besides, it has 
				conducted corporate get-togethers with active participation from 
				business associates. <br>
