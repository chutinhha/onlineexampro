						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">
						Investing in gold mining, oil shares</font></b><p class="style15">
				<strong><span class="style14">Gold’s role as a monetary currency 
				and a store of value and hedge against inflation and deflation 
				will only be amplified by the inexorable shift of financial 
				power from Wall Street and Europe to Asia<br>
				<br>
				<i><font color="#808080">BY Matein Khalid</font></i><br>
&nbsp;</span></strong><p align="justify" class="style13">
				Gold was the ultimate asset class in the past decade, rising 
				from $260 in 1999 (when the Bank of England sold its bullion 
				reserves under orders of the then Chancellor Gordon Brown!) to 
				$1,577 in May 2011. The debasement of the dollar under George W. 
				Bush after 9/11, the Federal Reserve’s zero interest rates and 
				successive liquidity creation sprees, the geopolitical shocks in 
				the Middle East, the Greek/Portuguese sovereign debt crisis, the 
				determination of central banks and private investors in the 
				emerging markets (particularly India and China) and speculative 
				flows from macro hedge funds all combined to create the most 
				spectacular bull run in gold since the 1970s.<br>
				<br>
				Yet it is important to remember that even secular bull markets 
				can have savage short term corrections, as gold did in summer 
				2006 and winter of 2008, after the failure of Lehman Brothers. I 
				am now nervous about gold at current levels or at least above 
				$1400. For, first of all, risk aversion on Wall Street and a 
				soft economy could hit prices. Secondly, the collapse of the 
				Gaddafi regime or another ECB/EU/IMF bailout of Greece would 
				reduce risk premia in the yellow metal. Besides, rising interest 
				rates in India, China, Russia and Brazil reduce the appeal of 
				gold to their citizens and sovereign wealth funds. Moreover, 
				Saudi Arabia’s decision to raise oil production will lead to 
				lower prices for oil and gold. George Soros has sold $800mn in 
				gold and publicly called it the “ultimate asset bubble”. Short 
				term, I agree. Long term? No way.<br>
				<br>
				It is entirely possible that gold could correct down to the 
				$1,350-1,450 range if there is a meltdown in the financial 
				markets. However, the macro forces that triggered the bull 
				market are not going to disappear. The European monetary union 
				has been devalued by Greece. The crisis in the Arab world has 
				escalated in Yemen, Syria and Libya. The Federal Reserve cannot 
				raise rates and tighten liquidity as long as one-third of 
				homeowner mortgages are underwater and the unemployment rate is 
				9.1 per cent. The US budget deficit is now a colossal $1.5trn 
				and Uncle Sam is de facto bankrupt. With the Euro’s Greek woes, 
				the only viable diversification option for dollar-overweight 
				central banks and sovereign funds in the Gulf, Southeast Asia, 
				China, India, Russia and Latin America is, after all, gold. Gold 
				has been a monetary currency for 2000 years because it is a 
				store of value and hedge against inflation, deflation, fickle 
				governments and reckless deficit spending. This role will only 
				be amplified by the inexorable shift of financial power from 
				Wall Street and Europe to Asia, the new hard money epicenter of 
				world finance at a time of negative dollar real interest rates. 
				It will not surprise me to see gold prices rise as high as 
				$1,800 in the next 12 months. The trend is your friend until the 
				trend comes to an end and this trend has not come to an end.<br>
				<br>
				Investors who are reluctant to buy a ten tola bar or a gold ETF 
				at current levels should consider gold mining shares, who have 
				lagged the performance of the gold prices despite a surge in 
				operating margins and earnings growth. I can easily find miners 
				whose valuations reflect $1,200 gold, not the $1,500 plus prices 
				in the spot market. The idea is to focus on miners with low 
				operating costs, long life reserves, no unquantifiable systemic 
				political risk (I definitely prefer exposure to Canadian 
				politics than, say, those of Congo or Kazakhstan!) and an 
				unhedged asset book.<br>
				<br>
				Goldcorp is one of the world’s leading gold mining companies, 
				with a dozen mines in the US, Canada, Mexico and Argentina. 
				Goldcorp trades on the NYSE under the symbol GG. The shares have 
				fallen from 56 as gold has corrected to 46 but I would ideally 
				wait for the 36-38 as the optimal entry point. Gold mining 
				output growth in Goldcorp is among the highest in the world and 
				increases in cash flow/dividends will be exponential, though its 
				valuation is not low at 17 times earnings. If Goldcorp falls to 
				36-38, the risk/reward calculus becomes compelling.<br>
				A cheaper alternative to Goldcorp is Newmont Mining, the world’s 
				leading gold miners. Newmont trades on the NYSE under the symbol 
				NEM. Newmont has also corrected from its 65 high and would be an 
				ideal buy in the 44-46 range where its valuation multiple would 
				be a modest 10 times earnings, as Newmont can deliver $4.25 EPS 
				in 2011, up from $3.80 in 2010. Newmont is trading well below 
				its historic valuation ranges and, if bought at 45, could well 
				enable long term investors to double their money in three years, 
				as I estimate the price will be between $90-100 in the summer of 
				2014 if my projections on cash flow, dividends, EPS are accurate 
				and gold/copper prices do not collapse.<br>
				<br>
				The message of the last OPEC meeting at Vienna is unmistakable. 
				Saudi Arabia has now decided to increase its production from the 
				current 8.7 million barrels per day (mbd) to 10 mbd by late 
				summer. The future price of black gold will be determined in the 
				royal palaces of Riyadh and Jeddah, not the glass tower OPEC 
				Secretariat in Vienna. The fall in oil prices will be amplified 
				by the end of Federal Reserve quant easing (Bernanke’s money 
				printing boosted the price of oil) and the unmistakable economic 
				slowdown in the US, China, Eurozone and emerging Asia. Oil 
				company shares have at least 10 per cent downside risk, even low 
				beta colossi such as Exxon and Royal Dutch Shell. Yet the 
				strategic rationale for owning Big Oil shares is a simple macro 
				equation. Global oil demand will grow at 2 million barrels per 
				day every year in the next five years while new supply growth 
				will be only one million barrels per day as long as global GDP 
				growth remains above 4 per cent. The mathematics of black gold 
				means the world economy will need 100 mbd sometime in the next 
				five years. This means even if oil prices fall in 2011, the 
				snapback will swift and the secular trend is (I believe) $160 
				Brent in five years. Exxon shares are valued at $85 West Texas 
				even now, though I would reinvest in the world’s leading 
				integrated oil super major in $74-76 range. Exploration 
				companies (Apache, Marathon Oil) will remain takeover 
				candidates. Offshore drilling, new seismic/drilling technologies 
				and the capex plans of state-owned oil giants (e.g. Aramco, 
				Rosneft, KOC, ADNOC, PDO) mean secular growth opportunities for 
				Schlumberger, Baker Hughes and Halliburton, the world’s leading 
				oil services companies.<br>
