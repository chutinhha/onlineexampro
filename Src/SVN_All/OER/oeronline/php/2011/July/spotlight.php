				<b><font size="2">Driven by ethos <br>
				</font></b><br>
				Al Madina’s underwriting philosophy is to reward positive risk 
				features; and its guiding <br>
				principles are quality and long-term relationships<br>
				<br>
				Al Madina is a well-established Insurance company in the 
				Sultanate known for its wide product offering, customer service, 
				focused marketing and top notch insurance professionals from 
				around the world including the best of talents from within Oman. 
				It is a composite insurance company offering both life and 
				non-life Insurance products in the Sultanate.<br>
				<br>
				Al Madina’s vision is to provide cost effective insurance and 
				superior customer service in a manner that takes into account 
				the culture, social ethos and needs of the community. The 
				company has the ability to listen to its customers, the 
				experience to understand their needs and the skillset to devise 
				the right solution that they require at a price which is 
				competitive. Clients’ needs and financial security are two most 
				important criteria factored in its insurance solutions.<br>
				<br>
				Al Madina’s underwriting philosophy is to reward positive risk 
				features. Its guiding principles are quality and long-term 
				relationships and not market share alone. With a significant 
				treaty capacity provided by a panel of leading reinsurers, it is 
				capable of providing wide acceptances, superior policy coverage 
				and competitive rating. The facultative markets are explored and 
				resorted to only when additional capacities are required. This 
				has been possible only due to its confidence in the risks it 
				underwrites. Its treaties are broadly worded with very few 
				restrictions giving an advantage of hassle-free covers to the 
				clients.<br>
				<br>
				Al Madina endeavours to set high standard of excellence and set 
				up a process that can deliver it consistently. To this end, it 
				provides service level agreements. The Turnaround Time (TAT) is 
				constantly being improved in every aspect of company’s business 
				– from documentation to claim servicing.<br>
				<br>
				The company today has a wide range of retail package products 
				including travel &amp; domestic helper (Housemaid) insurance and it 
				is continuously expanding the retail offering. Al Madina is 
				expecting to launch Individual health insurance product and home 
				package insurance in 2011, to add to the existing range of 
				retail products. It underwrites the retail SME business in a 
				smart centralised manner underpinned by advanced web-based 
				system. The coming years will see a concerted focus on the 
				package retail and SME business by creating a ‘one stop shop’ 
				for this segment.<br>
				<br>
				The company takes pride in establishing quite a few first-time 
				initiatives in the market. It has signed a Memorandum of 
				Understanding with world’s oldest and most respected insurance 
				company – Chubb for specialty line of business such as Financial 
				Liability. Al Madina is also a member of a regional engineering 
				cooperative called Takaful Engineering Cooperative (TEC) and its 
				sole representative in Oman.<br>
				<br>
				Al Madina believes that living up to the covers contained in the 
				policy issued is the very essence of its existence; and this 
				approach is reflected in its claims settlement which hinges on 
				three guiding principles: empathy, fairness and speed. It sets 
				its own transaction benchmarks and aims to make that known to 
				its customers and then deliver on it consistently.<br>
				<br>
				<b><font size="2">On a mission <br>
				</font></b><br>
				Oman Insurance Company is looking to be a recognised provider 
				with a passion for excellence and concern for people<br>
				<br>
				Oman Insurance Company (P.S.C.) established by Royal Amiri 
				Decree of 1975 is a recognised leading composite private insurer 
				in the GCC and the Arab World. Financially sound and 
				professionally managed, it is dually rated as A M Best (A) 
				Excellent, A M Best Issuer Credit Rating ‘a’ and as Standard &amp; 
				Poor’s (BBB+) Strong. It is also rated Brand Value A+ by Brand 
				Finance Middle East and listed amongst the top 25 companies in 
				the UAE. <br>
				<br>
				In 2010, Oman Insurance wrote AED 2.44bn in premiums across all 
				classes returning technical profits of AED 232m and annual net 
				profits of AED 94mn. Shareholders equity of AED1.45bn and assets 
				of AED 4.8bn further demonstrate the leadership, strength and 
				market leading capabilities of the company in the continuing 
				global downturn conditions of the world economy. <br>
				<br>
				Its vision is to be a recognised provider with a passion for 
				excellence and concern for people. Its mission is to provide 
				peace of mind, valued services; to nurture partnerships and 
				delight by integrating people process and technology. <br>
				<br>
				Headquartered at DAFZA Dubai and with over 15 branches, it has a 
				strong presence in every Emirate, Sultanate of Oman and Qatar. 
				Its qualified and experienced insurance professionals provide 
				quality insurance products from across individual personal line 
				policies to risk based solutions for industrial, commercial, 
				energy, and construction sectors. OIC underwrites all classes of 
				general and medical, life insurances and going that extra mile 
				to tailor make bespoke covers. Its insurance programmes are led 
				by world-class and world leading re-insurers.<br>
				<br>
				Its commitment to the Sultanate as a long-term partner is to 
				help benchmark, lead, create awareness, world-class service, 
				Omanisation, and build robust infrastructure. It has an R &amp; D 
				unit to facilitate development of innovative customer centric 
				products and solutions responsive to market needs of the Gulf 
				population. It takes pride for having been accredited with 
				awards and certifications over the years, to name a few, General 
				insurer Award (2005 and 2006), Training Initiatives Award (2005, 
				2006 and 2007), Life Insurer Award (2006) Corporate Social 
				Responsibility Award (2007), the prestigious Mohamed Bin Rashid 
				Al Maktoum Business Award in the Financial sector (2007) and the 
				latest AON Hewitt award for ‘Best Employer In The Middle 
				East-2011’.It continues to work towards greater market 
				recognition across all aspects of its business, training, 
				responsible HR practices, best-practice, good governance, social 
				responsibility, responsible charity, CRM, innovation and market 
