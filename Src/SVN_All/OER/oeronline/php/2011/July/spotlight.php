						<p class="style15">
				<span class="style14"><strong>As a method designed to offer 
				protection from the financial consequences of various disasters 
				and uncertainties in life, insurance forms a vital part of sound 
				financial planning</strong></span><p align="justify" class="style13">
						<p align="justify" class="style13">
				<br>
				<b><font color="#CC3333" size="2">Safeguarding against 
				uncertainties</font></b><p align="justify" class="style13">
				<br>
				The genesis of insurance can be traced back to the appearance of 
				human societies on the surface of the earth. Chinese and 
				Babylonian traders had practiced the early methods of 
				transferring or distributing risk as long ago as the 3rd and 2nd 
				millennia BC, respectively. While they were travelling 
				treacherous rivers, Chinese merchants would redistribute their 
				wares across many vessels to limit the loss in the event of any 
				single vessel’s capsizing.<br>
				<br>
				The Babylonians developed a system which was recorded in the 
				famous Code of Hammurabi, c. 1750 BC, and practised by early 
				Mediterranean sailing merchants. As a part of the deal, if a 
				merchant received a loan to fund his shipment, he would pay the 
				lender an additional sum in exchange for the lender’s guarantee 
				to cancel the loan should the shipment be stolen or lost at sea. 
				Insurance is designed to protect people from disasters and their 
				financial consequences. There are many kinds of insurance and 
				the most important of these is considered to be life insurance 
				which makes financial provision for your family following your 
				death.<br>
				<br>
				As there are various financial commitments, you need to meet in 
				life you also need to provide something even in death to ensure 
				the security of the family, and to help them meet expenses at 
				least for a short time, to protect dependent parents or to 
				provide security for your spouse and children. These financial 
				obligations could well include funeral expenses, unsettled 
				hospital and other medical bills, mortgage payments, business 
				commitments and meeting the college expenses of one’s children.<br>
				<br>
				In law and economics, insurance is a form of risk management 
				primarily used to hedge against the risk of a contingent, 
				uncertain loss. Insurance is defined as the equitable transfer 
				of the risk of a loss, from one entity to another, in exchange 
				for payment. An insurer is a company selling the insurance; an 
				insured or policyholder is the person or entity buying the 
				insurance policy. The insurance rate is a factor used to 
				determine the amount to be charged for a certain amount of 
				insurance coverage, called the premium. Risk management, the 
				practice of appraising and controlling risk, has evolved as a 
				discrete field of study and practice. The transaction involves 
				the insured assuming a guaranteed and known relatively small 
				loss in the form of payment to the insurer in exchange for the 
				insurer’s promise to compensate (indemnify) the insured in the 
				case of a large, possibly devastating loss. The insured receives 
				a contract called the insurance policy which details the 
				conditions and circumstances under which the insured will be 
				compensated.<br>
				<br>
				<font size="1" color="#CC3333">Peace of mind</font><b><font color="#008000" size="2"><br>
				</font></b><br>
				Precisely how much insurance you will need varies depending on 
				your lifestyle, financial needs and sources of income, debts and 
				the number of dependents you are responsible for. In the main, 
				an insurance adviser or agent would recommend that you take 
				insurance cover that is five to ten times your current yearly 
				income. An important part of your financial planning, whole life 
				insurance gives you peace of mind for any uncertainties in life.<br>
				<br>
				Adequately planned life insurance will provide funds in the case 
				of unexpected death to deal with debts, mortgage payments and 
				day-to-day living expenses. It offers protection to the family 
				that one leaves behind and serves as a cash resource. It secures 
				your estate on death by providing tax free cash which can be 
				utilised to pay estate and other death duties. Life insurance 
				policies can also have a savings or pension provision which can 
				help to fund you during retirement.<br>
&nbsp;<p align="justify" class="style13">
				<br>
				<font color="#CC3333" size="1">Understand riders</font><b><font color="#008000" size="2"><br>
				</font></b>
				<br>
				Some policies have riders such as restricted coverage of term 
				insurance or critical illness for the children or spouse. There 
				are particular rules considering eligibility for riders, which 
				you will have to clearly understand.<br>
				<br>
				In case of bankruptcy the cash value, together with the death 
				benefits of any insurance policy is exempt from your creditors. 
				Holding a valid insurance plan is considered as having a 
				financial asset and this will improve your credit rating when 
				you need medical insurance or a home loan or business loan. 
				Insurance forms a vital part of sound financial planning but you 
				do have to evaluate both your personal risk and your longer term 
				commitments. Plans, like a whole life insurance plan, give you 
				the security you need for your dependents and also act as a good 
				form of financial security against which you are able to borrow.<p align="justify" class="style13">
				&nbsp;<p align="justify" class="style13">
				<b><font size="2"><font color="#CC3333">Strong and steady</font><br>
				</font></b><br>
				<b>Oman’s insurance sector remained relatively insulated mainly 
				because of the strong financial regulations of the country, 
				despite the global financial crisis hitting worldwide financial 
				institutions</b><br>
				<br>
				Oman’s insurance sector was less affected by the global 
				financial crises and its impact on the financial markets and 
				banking and real estate sectors because insurance companies have 
				diversified investments. Because of the strong regulatory 
				environment of Oman, insurance investments are governed by the 
				Insurance Companies Assets Investment Regulation issued by the 
				CMA in April 2007 which requires that the investments of local 
				insurance companies shall not exceed 25 per cent of the total 
				investments and the cash shall not be less than 40 per cent of 
				such per cent. Investments in shares shall not exceed 30 per 
				cent and in real estate 20 per cent of the allowed investment 
				abroad.<br>
				<br>
				Thus the companies maintained their solvency that enabled them 
				to continue their business without any exposure to the credit 
				crunch. The Capital Market Authority (CMA) continued its efforts 
				to develop the insurance sector. It achieved remarkable progress 
				in the regulatory supervision through the application of 
				international standards and continued enhancement of legislative 
				infrastructure and building institutional capabilities of the 
				sector, diversification of the services provided to the 
				participants of the sector, easing the processes and upgrading 
				the skills and efficiency of the employees.<br>
				<br>
				Oman has a backlog of financial assets to enable it to ramp up 
				expenditure on key economic projects. The government’s gross 
				financial assets plus the central bank’s foreign exchange 
				reserves amounted to around 75 per cent of GDP, according to 
				Moody’s estimates. Overall debt levels remain comparatively 
				small.<br>
				<br>
				In order to develop and modernise the insurance sector the 
				Insurance Companies Law was reviewed to cope with the 
				requirements of the current stage to mitigate market risks, 
				protect market participants and improve the services. CMA 
				amended the Executive Regulation of the Insurance Companies Law 
				and the Council of Ministers issued a decision increasing the 
				amount of “Diyyah” (Blood money) to RO15,000.<br>
				<br>
				A number of new guidelines and regulations were issued to 
				insurance companies to streamline the insurance industry in 
				Oman. The CMA issued a circular to all insurance companies on 
				the new guidelines for submitting new general insurance products 
				to ensure compliance with the requirements of Insurance 
				Companies Law, the Regulation, the Code of Corporate Governance 
				for Insurance Companies and the Code of Professional Conduct on 
				provision or marketing of new general insurance products. The 
				guidelines require that the insurance products shall comply with 
				the Code of Professional Conduct of Insurance Business issued by 
				CMA.<br>
				<br>
				Design and rating of products shall be on sound and prudent 
				underwriting principles and the risks insured under the product 
				shall be clear and transparent without any ambiguity.<br>
				<br>
				With an annual premium of $626 million, the insurance sector has 
				witnessed healthy growth in Oman and the sector has posted a 
				robust 10 per cent growth. The insurance sector is likely to 
				look up further with better market scenario in 2010 and 
				presentation of development oriented state budget and 8th 
				Five-Year-Plan. <br>
				<br>
				<b><font size="2">Major players </font></b><br>
				<br>
				According to the latest report prepared by Business Monitor 
				International, the total premium income for 2010 was around 
				RO231mn – RO191mn constituted by non-life segment and RO39mn by 
				the life segment. By 2015, according to the report, the 
				corresponding figures will touch RO327mn, RO274mn and RO53mn, 
				respectively. <br>
				<br>
				The Sultanate has 23 insurance firms - 11 local and 12 foreign 
				insurers. The locally incorporated or national insurance firms 
				have a combined market share of around 80 per cent. Among this, 
				four firms – Dhofar Insurance, Al Ahlia (which recently merged 
				with Royal &amp; Sun Alliance Oman), National Life and Oman United 
				Insurance – have a major share of the market. <br>
				<br>
				As of now, only motor insurance is compulsory by law and as such 
				majority of the other insurance is dictated by the requirements 
				of the businesses or lenders. This is also a segment that 
				witnesses major undercutting of premium, thereby bringing down 
				margins.<br>
				<br>
				In the life segment, healthcare insurance is fast becoming a top 
				priority for local companies. Both small and large companies are 
				keen on taking group medical policy for their employees. 
				Healthcare insurance is a cashless scheme wherein the insurance 
				company, which ties up with a network of hospitals/polyclinics, 
				gets volume business and the employees get access to cashless 
				healthcare facilities. Once the company pays its group medical 
				premium, there is no administrative work for the company and all 
				worries related to employee healthcare would rest with the 
				insurance company and the concerned hospital.<br>
				<br>
				<b><font size="2">Omanisation</font></b> <br>
				<br>
				With a total workforce of 1,800, the insurance sector in the 
				Sultanate had achieved an Omanisation level of 60 per cent. The 
				Capital Market Authority (CMA) has urged all companies operating 
				in the insurance sector to set a timeframe for the Omanisation. 
				The companies are required to decide the target number of 
				citizens to be appointed, as well as the nature of jobs 
				available.<br>
				<br>
				The step is aimed at securing employment opportunities for 
				job-seekers in response to the Royal orders. His Majesty Sultan 
				Qaboos had ordered the employment of 50,000 citizens whose names 
				are listed in the job-seekers register of the Ministry of 
				Manpower. The Royal orders are aimed at providing decent living 
				for the Omani citizens, availing them an opportunity to serve 
				their country and achieve stability for the Omani families.<br>
				<br>
				The CMA sent a circular to insurance companies and insurance 
				brokers urging them to raise the Omanisation of citizens to 65 
				per cent by 2012. It also urged them to provide good working 
				opportunities for the national manpower to help benefit from the 
				current boom in the insurance sector.<br>
				<br>
				<br>
				<b><font size="2">PLUGGS</font></b><br>
				<br>
				Building momentum post integration<br>
				One of Oman’s leading insurers, Al Ahlia Insurance Co has a wide 
				range of products <br>
				designed for both individual and corporate clients<br>
				<br>
				Al Ahlia Insurance Co. is one of the oldest insurance companies 
				in the non-life insurance sector in Oman. Established in 1985 as 
				a public joint stock company, Al Ahlia Insurance Co. was 
				restructured in 1998 as a closely held subsidiary of its holding 
				company following substantial growth in invested funds. In 2010, 
				the company was acquired by RSA, and has since become part of 
				the globally reputed RSA group and the second largest insurance 
				company in Oman.<br>
				<br>
				The newly formed legal entity, Al Ahlia Insurance looks forward 
				to build on the momentum through RSA’s technical capability, the 
				nationwide network of 23 branches and capitalising on its 
				personal lines business. Being one of Oman’s leading insurers, 
				its products are designed for both individual and corporate 
				clients. The portfolio ranges from motor to home insurance for 
				individuals to shop insurance and managing large and complex 
				risks for the corporate sector. The company has the experience 
				and skill-set to devise the right solutions for clients at a 
				market driven competitive price. <br>
				<br>
				Al Ahlia Insurance constantly endeavors to set high standards of 
				service excellence for its customers. The response time and 
				processes are constantly being monitored and improved in every 
				aspect of company’s business transactions – from proposal stage 
				to underwriting to documentation and claim disbursals. It is 
				this attention to the entire customer journey that helps in 
				creating and building enduring long-term relationships.<br>
				<br>
				Al Ahlia plays an important role in the insurance industry in 
				Oman. Its products are constantly being enhanced to provide 
				value to customers and address key risk needs of clients. As a 
				part of its social responsibility strategy, Al Ahlia has plans 
				to partner with key bodies to launch initiatives which can help 
				create a safer environment, for the overall benefit of society.<br>
				Al Ahlia has been honoured with the inaugural ‘Oman Insurer of 
				the Year 2011’ award at the prestigious annual MENA Insurance 
				Awards by Gulf Insurance Review magazine. It was chosen for the 
				Oman Insurer of the Year award because of its coherent strategy, 
				product innovation, responsiveness to customer needs, enhanced 
				service delivery and successful management of change.<br>
