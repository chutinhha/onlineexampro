				coverage and word of mouth goodwill generated by OGA has helped 
				to raise environment consciousness in the Sultanate. A number of 
				other organisations have initiated environment conservation 
				efforts in its trail and it is heartening to see the Green Oman 
				movement gaining traction.<br>
