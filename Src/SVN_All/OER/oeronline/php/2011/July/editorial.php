				<br>
				The promise offers a big business opportunity for companies. The 
				Middle East region is catching up rapidly in the IT sweepstakes. 
				According to a recent report from the Economist Intelligence 
				Unit, the Middle East IT services market is likely to hit $3.5bn 
				by 2012. There are 41.8mn internet users across 16 Arab 
				countries with Arabic emerging as the seventh most popular 
				language on the net. Oman has been going through its own digital 
				revolution. The IT services market is poised to grow by 10 per 
				cent reaching $0.53bn by the end of 2011. According to Arab 
				Advisory Group data, cellular penetration in Oman stands at 
				132.9 per cent. This growth has made Oman an attractive IT 
				destination for local, regional and international companies. 
				Given the right enablers, the sector has the potential to emerge 
				as a regional powerhouse. This issue’s cover story gives a few 
				pointers in this direction. <br>
