				on May 4 in 1989) has represented Ireland as both an amateur and 
				a professional. He had a successful amateur career, topping the 
				World Amateur Golf Ranking for one week as a 17-year-old in 
				2007. Later that year he turned professional and soon 
				established himself on the European Tour. He had his first win 
				on the European Tour in 2009, and on the PGA Tour in 2010. He 
				has featured in the top ten of the Official World Golf Rankings 
				and represented Europe in the Ryder Cup.<br>
				McIlroy now has one major championship to his name. The US Open 
				was McIlroy’s first major win and just his third victory 
				overall. The temptation to ask how many more is hard to resist 
