				</span></strong><p align="justify" class="style13">
				By winning the 2011 US Open in a style, Rory McIlroy has earned 
				favourable comparison with some of the game’s greatest 
				performances. The 22-year-old posted a final round of 69 to wrap 
				up an eight-shot success with a record low score for the 
				tournament of 16-under par. McIlroy’s winning score of 268 was a 
				record for an event that has been staged 111 times and competed 
				for by every great golfer who has lived during that time.<br>
				<br>
				With this victory on June 20, Rory McIlroy has followed the 
				disappointment of his Augusta National collapse two months ago 
				with a display that will go down in the annals alongside that of 
				Tiger Woods at Pebble Beach in 2000 – a performance acknowledged 
				by many as the greatest in the tournament’s history. But the 
				dominance shown by the Northern Ireland player was on a par.<br>
				<br>
				His youth, his dominance and his charisma invited the obvious 
				comparisons with Woods, who was 20 when he won the 1997 Masters 
				by an even dozen and turned the world of golf upside down. 
				However, when McIlroy was busy shattering 12 records and winning 
				the US Open by a jaw-dropping eight shots at Congressional 
				Country Club, the ailing Woods was nowhere to be found. He also 
				stripped the Congressional course of its brutish reputation. The 
				US Open is not meant to be this easy but McIlroy made it so. 
				“The whole week has been incredible. I couldn’t have asked for 
				more,” he says, cradling the famous trophy in his arms.<br>
				<br>
