				&nbsp;<p align="center" class="style13">
				<font face="Verdana" size="1">
				<img border="0" src="images/event-july-1.jpg" width="450" height="285" style="border: 1px solid #808080; "></font><p align="justify" class="style13">
				<br>
				Top achievers in Oman’s corporate world were felicitated with 
				AIWA Awards 2011 for Best Performing Companies at a gala event 
				at Shangri-La Barr Al Jissah Resort &amp; Spa. The awards have been 
				instituted by Alam al-Iktisaad Wal A’mal (AIWA) Oman’s leading 
				Arabic monthly business magazine, published by United Media 
				Services (UMS).<br>
				<br>
				Held under the auspices of HE Dr Rasheed bin Al Safi Al Huraibi, 
				chairman, Tender Board and in association with Capital Market 
				Authority, AIWA Awards 2011 attracted the top echelons of the 
				corporate community and senior government officials in the 
				Sultanate. The high profile event was powered by Voltamp, Vertu 
				from Khimji’s Watches, BankMuscat, Hyundai Centennial and 
				Samsung.<br>
				<br>
				Delivering the key note address, HE Yahya Bin said Al Jabri, 
				executive president, CMA says, “AIWA Awards reflect positively 
				on the business environment and national economy apart from the 
				winning companies. This award is an encouragement to the 
				companies to become more competitive and improve upon their 
				performance. This quest for leadership will ultimately 
				strengthen the national economy.” On the occasion, UMS’ chief 
				executive Sandeep Sehgal states, “Our endeavor behind AIWA 
				Awards 2011 is to celebrate the success of Oman’s corporate 
				sector that has continued to excel despite challenges from 
				different quarters and has met its responsibilities towards the 
				shareholders and society admirably.”<br>
				<br>
				The event was marked by an amazing display of violin playing in 
				a bubble by a renowned international artist. Towards the end, an 
				enthralling performance by an all-Oman Orchestra group left the 
				august audience gasping for more. AIWA Awards 2011 was supported 
				by Al Shabiba, Times of Oman, Sablat, Al Wisal, Merge FM, Ruwi 
				Modern Printing Press (RMP), SPANCO, Gulf Brands and Mawarid 
				Trading.<br>
