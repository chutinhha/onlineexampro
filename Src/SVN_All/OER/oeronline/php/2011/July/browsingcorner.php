						Contrarian call</font></b><p align="justify" class="style3">
				<span class="style5"><strong><font color="#808080"><i>John 
				Paulson</i> </font>saw opportunity in adversity and made history 
				in the bargain. This book is the story of his daring gamble and 
				momentous triumph. An OER review<br>
				<br>
				</strong>
				</span>
				<br>
				<font face="Verdana" size="1">
				<img border="0" src="images/browsingcorner-july-1.jpg" style="border-style:solid; border-color:#808080; float: right" class="style4" width="200" height="291"></font>The 
				subprime crisis and its aftermath has fuelled numerous books on 
				how the global economy was crippled by unscrupulous bankers; 
				greedy fund managers and incompetent regulators. Unlike other 
				books which were about failures, Gregory Zuckerman details the 
				triumph of one man amidst the unfolding crisis.<br>
				<br>
				In 2006, hedge fund manager John Paulson realised something few 
				others suspected – that the housing market and the value of 
				subprime mortgages were grossly inflated and headed for a major 
				fall. Paulson’s background was in mergers and acquisitions, 
				however, and he knew little about real estate or how to wager 
				against housing. He had spent a career as an also-ran on Wall 
				Street. But Paulson was convinced this was his chance to make 
				his mark. He just wasn’t sure how to do it. Colleagues at 
				investment banks scoffed at him and investors dismissed him. 
				Even pros sceptical about housing shied away from the 
				complicated derivative investments that Paulson was just 
				learning about. But Paulson and a handful of renegade investors 
				such as Jeffrey Greene and Michael Burry began to bet heavily 
				against risky mortgages and precarious financial companies.<br>
				<br>
				<font color="#000080" size="2"><b>Right man at the right place</b></font><br>
				<br>
				Timing is everything, though. Initially, Paulson and the others 
				lost tens of millions of dollars as real estate and stocks 
				continued to soar. Rather than back down, however, Paulson 
				redoubled his bets, putting his hedge fund and his reputation on 
				the line.<br>
				<br>
				In the summer of 2007, the markets began to implode, bringing 
				Paulson early profits, but also sparking efforts to rescue real 
				estate and derail him. By the end of the year, though, John 
				Paulson had pulled off the greatest trade in financial history, 
				earning more than $15 billion for his firm--a figure that 
				dwarfed George Soros’s billion-dollar currency trade in 1992. 
				Paulson made billions more in 2008 by transforming his gutsy 
				move. Some of the underdog investors who attempted the daring 
				trade also reaped fortunes. But others who got the timing wrong 
				met devastating failure, discovering that being early and right 
				wasn’t nearly enough.<br>
				<br>
				The scale of Paulson’s big bet, “the greatest trade ever”, as 
				Greg Zuckerman describes it, was extraordinary. By piling into 
				complex “credit default swaps” against mortgages – in effect, 
				insurance policies that would pay out if homeowners defaulted – 
				his fund made an unthinkable $15bn in a year, $4bn of which he 
				took home himself.<br>
				<br>
				Zuckerman also tells the stories of others who placed similar, 
				much smaller trades, such as west coast property developer 
				Jeffrey Greene, a friend of Paulson’s before they fell out over 
				his refusal to invest in his hedge fund. All these pessimistic 
				investors were mavericks or outsiders, underlining how tough it 
				was in those heady days to stand alone against the herd of 
				charging bulls.<br>
				<br>
				Written by the prizewinning reporter who broke the story in “The 
				Wall Street Journal,” “The Greatest Trade Ever “is a superbly 
				written, fast-paced, behind-the-scenes narrative of how a 
				contrarian foresaw an escalating financial crisis--that 
				outwitted Chuck Prince, Stanley O’Neal, Richard Fuld, and Wall 
				Street’s titans – to make financial history.<br>
