						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">
						The global crisis and GCC shares</font></b><p class="style15">
				<strong><span class="style14">The market convulsions of August 
				2011 is expected to cast a long shadow on the emerging markets, 
				including the GCC stock exchanges . By Matein Khalid<br>
				<br>
				<br>
				</span></strong>The global financial trauma of August 2011 has 
				taken its toll on emerging markets, the ultimate risk asset in a 
				world fleeing from risk. Yet it is ironic that, unlike August 
				1998, when the Russian rouble devaluation and the Asian currency 
				meltdown horrified Wall Street, the current market mayhem stems 
				from the West-America’s credit downgrade, the Eurozone debt 
				contagion, and the speculative bear attack on the French banking 
				system. It is never wise to ignore the hoofbeats of the herd. 
				Oil prices have plunged by $30. Global economic growth rates 
				will fall. The Federal Reserve has committed to zero interest 
				rates till 2013. Goldman Sachs has estimated the risk of a US 
				recession at one-third. The market convulsions of August 2011 
				will cast a long shadow on the emerging markets, including the 
				GCC stock exchanges, this autumn.<br>
				<br>
				The spike in the Chicago Volatility Index (VIX) and surge in the 
				dollar are two classic indicators of risk aversion in financial 
				markets. In the GCC, the fall in oil prices is the most 
				important variable. A soft patch in the US is hardly a full 
				blown global recession. It is unlikely that oil price will fall 
				to $40, as it did after the failure of Lehman Brothers and the 
				onset of global recession. Even if US GDP growth is flat, Asian 
				oil demand can well increase by at least one million barrels a 
				day (MBD). The key variable is, of course, Saudi Arabia, the 
				central bank of black gold. The Saudis increased their 
				production to 10 million barrels a day in June after the last 
				OPEC meeting to prevent an oil price spiral. So a Saudi output 
				cut would put a floor under oil prices, possibly at $75 – 85 
				Brent. This means the downside risk on GCC stock markets is 
				limited, that a secular bear market is not on the horizon.<br>
				<br>
				<font color="#CC3333" size="2"><b>Exposed sectors in GCC </b>
				</font><br>
				<br>
				The most exposed sectors in the GCC during a global correction 
				are high beta banks, property developers, construction companies 
				and oil service firms. The liquidity risk premium also rises, so 
				small cap and illiquid shares get hit the hardest. Resist the 
				temptation to seek safe haven in GCC sovereign or corporate debt 
				(sukuk, Eurobonds). Credit spreads widen as deflation shocks 
				lead to lower Brent prices at the precise moment GCC states 
				budget breakeven oil prices have risen due to the events in the 
				Arab world since January 2011. It is also unwise to invest in 
				the debt of highly leveraged or lower rated issues.<br>
				<br>
				The optimal strategy, in my opinion, is to scan the universe of 
				low beta, defensive, high dividend telecoms not facing margin 
				pressure, ideally companies with stellar balance sheet, 15 – 25 
				per cent EPS growth, no margin pressure or legacy growth 
				businesses. Free cash flow yield is a classic metric of value 
				during troubled times. Yet even telecom valuations in the 
				emerging markets plummet during an emerging markets free fall. 
				Moreover, as oil prices fall, GCC economic growth could well 
				soften, meaning EPS could well fall by 10-15 per cent. This 
				factor must not be ignored by investors when making an “entry 
				point” decision. A share with ostensibly low valuations could be 
				expensive if earnings fall as a result of a softer economy or 
				even sector competition. As the largest telecom market in the 
				Middle East, Saudi Arabian telecoms exhibit defensive metrics.<br>
				<br>
				Saudi Telecom, the incumbent operator, is a highly complex 
				company with a huge capex expansion plan and a low margin, low 
				growth legacy voice business. Its earnings per share growth 
				prospects simply cannot be compared with Mobily, the 
				broadband/data centric, second mobile phone operator. Saudi 
				Telecom has spent capital buying stakes in telcos across Africa, 
				the Arab world and Southeast Asia. Saudi Telecom has raised 
				stakes in Axis (Indonesia) and its African ventures. Huge capex, 
				global dealmaking and a complex balance sheet add an uncertainty 
				risk premium to Saudi Telecom during times of market stress. 
				Mobily is a higher revenue growth, impressively profitable, high 
				free cash flow yield (11 per cent) and broadband/high speed data 
				business. Softer Saudi GDP growth and periodic price wars are 
				inevitable but I doubt if Mobily will fall&nbsp; much below 45SR 
				even in a severe correction as long as Saudi retail investor are 
				convinced that it can deliver a 3 per cent dividend.<br>
				<br>
				The Brent crude oil price has at least $20 downside risk, 
				possibly down to $75 – 85 range. This means that the correction 
				in GCC can continue in September. Yet Saudi Arabia and OPEC can 
				avoid a free fall with a coordinated output cut, particularly 
				since the world is operating on minimal spare capacity. A Saudi 
				output cut on a credible US budget deficit reduction plan could 
				well lead to a sentiment U-turn in the GCC stock markets. Yet 
				this is not 2008, when bottom fishing was a deadly value trap. 
				The GCC is not headed for recession, even if economic growth and 
				earnings could well decline. Raise cash. <br>
				Stay defensive.<br>
