						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">
						Necessity rather than a choice</font></b><p align="justify" class="style3">
						<span class="style5"><strong>Dedicated to future global 
						managers, The Global You dovetails ten strategies for 
						achieving success as a working expat. <br>
						<br>
						<font color="#808080"><i>An OER review</i></font><br>
						<br></strong></span><br><font face="Verdana" size="1">
						<img border="0" src="images/browsing1-sep2011.jpg" style="border-style:solid; border-color:#808080; float: right" class="style4" width="200" height="280"></font>Millions 
						of people are now global business workers of some kind. 
						Even though they might not use the term as such, they 
						are buying, selling, managing and operating all over the 
						world, but often have little preparation for the job. 
						This book provides ten practical ways to enable the 
						‘global you’ to think, behave and operate in the 
						appropriate global way. The 10 strategies here, based on 
						extensive research undertaken by the authors on 
						successful global players, are designed to help anyone 
						cope with cultural challenges and, just as important, 
						the practical implications of operating beyond familiar 
						environments. From thinking global to learning to work 
						in a multicultural context and updating your 
						communications skills to learning to manage your time 
						across time zones , The Global You is both informative 
						and practical for anyone whose work takes them out of 
						their home country.<br>
						<br>
						The authors provide a ten-step plan for becoming more 
						globally aware and better able to work with people of 
						other beliefs, customs and languages. These steps 
						include:<ul>
							<li>
							<p align="justify" class="style3">Think global</li>
							<li>
							<p align="justify" class="style3">Learn to work in a 
							multicultural environment </li>
							<li>
							<p align="justify" class="style3">Travel</li>
							<li>
							<p align="justify" class="style3">Learn a language</li>
							<li>
							<p align="justify" class="style3">Learn how to learn 
							when you’re outside the classroom</li>
							<li>
							<p align="justify" class="style3">Go virtual but 
							stay ‘real’</li>
							<li>
							<p align="justify" class="style3">Treat 
							multicultural teamwork as a core skill</li>
							<li>
							<p align="justify" class="style3">Build your network</li>
							<li>
							<p align="justify" class="style3">Raise your global 
							profile</li>
							<li>
							<p align="justify" class="style3">Manage your time 
							when dealing with friends, family and colleagues in 
							different time zones</li>
						</ul>
						<p align="justify" class="style3">Each chapter contains 
						real-life examples from global workers’ lives, 
						suggestions and exercises.<br>
						<br>
						<b><font size="2" color="#C00000">In a new guise</font></b><br>
						<br>
						The book reiterates the fact that we live in a complex 
						world and both companies and individuals need to break 
						out of their national silo’s and develop a global state 
						of mind.<br>
						<br>
						The Global You repeatedly makes the point that thinking 
						globally is a discipline which extends from learning new 
						languages to attending funerals in Soweto or weddings in 
						Bombay.<br>
						<br>
						An interesting section challenges the widespread belief 
						that people hate change, noting that many of us are 
						fascinated by innovation, news and gossip. Not everybody 
						will agree with this observation; some of us like change 
						and some don’t but it is an interesting corrective to 
						the general view, common in management and politics that 
						change is impossible.<br>
						<br>
						The authors point out in the introduction: “Even if you 
						want to work for a “national” company, that option is 
						rapidly disappearing. You are part of the global 
						economy, and most probably a multi-cultural working 
						experience, whether that was your career plan or not.” 
						And The Global You provides a simple blue print for 
						those who would like to train themselves to think beyond
						<br>
						their shores.<br>
