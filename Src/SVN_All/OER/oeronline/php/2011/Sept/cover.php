						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">
						A return of confidence</font></b><p class="style15">
				<span class="style14"><strong>Creation of new jobs, higher 
				salaries and enhanced government spending is generating momentum 
				in the Sultanate’s economy as reflected in the corporate 
				sector’s performance during the first six months of 2011. A 
				report by <i>Mayank Singh</i></strong></span><p align="justify" class="style13">
				<b><font size="7">A</font></b>l-Seeb Technical Establishment (SARCO), 
				the distributors of Samsung products in Oman witnessed a 40 per 
				cent year-on-year (YoY) growth during the first six months of 
				2011 (H1’2011) compared to the corresponding period in 2010. 
				This growth was largely driven by the sale of IT products like 
				notebooks, laptops, printers; mobility solutions (smart phones, 
				tablets) and the audio video segment.<br>
				<br>
				The Zubair Automotive Group has seen a brisk sale of vehicles in 
				H1’ 2011 and it expects to close the years with better numbers 
				than 2009 and 2010. Encouraged by this success, the group has 
				launched a range of new models in the market.<br>
				<br>
				Al Jazeera Steel Products Company has seen a 27 per cent growth 
				in production and 30 per cent rise in sales during H1’2011. It’s 
				revenues shot up by 46 per cent and profits are up by 86 per 
				cent. The improvement in the GCC markets, strong demand from 
				North American markets for its tube mill products and its larger 
				spectrum of offerings have all contributed to its sterling 
				performance. Ahli Bank, BankMuscat, Bank Sohar and National Bank 
				of Oman have seen their total revenues grow by 39.5 per cent, 
				20.5 per cent, 20.1 per cent and 12.1 per cent respectively 
				during H1’2011. Their net profits too are up by 30.3 per cent, 
				21.7 per cent, 14.8 per cent and 16.9 per cent respectively. 
				Gross loans and advances, customer deposits and savings deposits 
				have all seen a steep increase.<br>
				<br>
				Shell Oman Marketing Company’s total revenue increased from 
				RO156.30mn in H1’2010 to RO185.36mn in H1’2011 – an increase of 
				18.6 per cent. Net profit too jumped from RO7.2mn in 2010 to 
				RO7.9mn – a spike of 9.6 per cent. The oil retailing major’s 
				other financial ratios are equally impressive – gross profit 
				margins stood at 10.3 per cent, total shareholder returns at 
				11.7 per cent, while return on average capital employed was 60 
				per cent.<br>
				<br>
				The examples mentioned above point out a single truism about 
				Oman’s business environment – while the empty half of the glass 
				may be occupying more mind space, the glass is actually half or 
				more full. Different companies from diverse sectors operating 
				across multiple markets in Oman are seeing strong growth and 
				demand. On an aggregate basis the total revenues of the MSM 30 
				companies grew from RO1,298.93mn in H1’2010 to RO1,462.49mn in 
				H1’2011 – a jump of 12.6 per cent. This topline growth of the 
				index was aided by the higher than expected performance of the 
				banking and industry sectors which grew by 17 per cent and 17.6 
				per cent YoY during H1’2011.<br>
				<br>
				<font color="#C00000" size="2"><b>The prime mover</b></font><br>
				<br>
				<font color="#000080" size="7"><b>
				<img border="0" src="images/cover1-sep2011.jpg" width="150" height="164" align="left" style="border: 2px solid #C0C0C0"></b></font>So 
				what accounts for this new found confidence? A large part of 
				this self assuredness stems from the measures taken by the 
				government to address the grievances raised during the protests 
				in January and February. Initiatives like creating 50,000 jobs 
				for Omanis, raising minimum salary levels to RO200, unemployment 
				allowance provision of RO150 per month and increase in social 
				security benefits have led to enhanced spending power giving a 
				fillip to retail activity.<br>
				<br>
				A look at the revised budgetary numbers puts the magnitude of 
				this additional financial prowess in perspective. In April 2011, 
				HE Darwish bin Ismail al Balushi, the Minister Responsible for 
				Financial Affairs announced the government’s decision to earmark 
				RO1bn in additional funding towards employment generation, 
				unemployment pay-outs, cost of living allowances, enhanced 
				pension and social security benefits. Elaborating on the 
				details, the minister said that RO444mn was being allocated for 
				providing employment to 40,714 people; RO90mn for unemployment 
				allowance; RO43mn as pension for retired civilian and defence 
				employees; RO251mn as cost of living for employees in military 
				and civilian positions; RO25mn for students; RO25mn as pension 
				for Omanis in the private sector and RO14mn for supporting 
				sports and the youth sector.<br>
				<br>
				As a result of these new financial commitments, the public 
				expenditure increased to about RO9.1bn, markedly up from RO8.1bn 
				announced in the state budget 2011. The Sultanate expects to 
				fund the additional RO1bn from oil revenues earned above the $58 
				per barrel benchmark set by the government. Says Anil C Kapadiya, 
				country manager, Oman Insurance Company, “In the wake of the 
				Arab Spring, a lot of soft cash has come into the market as a 
				result of the government’s largesse. The RO1bn stimulus will not 
				be pumped into the market in one day, but will be staggered in 
				tranches of RO85-100mn every month and this will have an impact 
				on retail activity.”<br>
				<br>
				The efforts taken by the private sector in response to His 
				Majesty’s call for employment generation, has also added to this 
				new found momentum. According to the Ministry of Manpower 
				reports – 32,473 Omani nationals have been employed by the 
				private sector from February to July 2011. Overall, 74,917 jobs 
				have been created for nationals across the economy. Says 
				AbdulRazak Ali Issa, chief executive, BankMuscat, “The economic 
				conditions in Oman continue to be good with higher liquidity, 
				new employment creation, increased government spending and 
				enhanced consumer spending.”<br>
				<br>
				<font color="#000080" size="7"><b>
				<img border="0" src="images/cover8-sep2011.jpg" width="150" height="162" align="left" style="border: 2px solid #C0C0C0"></b></font>More 
				employment has translated into greater purchasing power and this 
				finds a reflection in the sale of automobiles, FMCG goods, 
				consumer durables etc. Says Aftab Patel, CEO, Al Omaniya 
				Financial Services (AOFS), “In the last two months the demand 
				for car loans has gone up by 25-30 per cent and the same holds 
				true for consumer product loans. Trading business too has gone 
				up significantly giving a headstart to trade finance.”<br>
				<br>
				The automobile industry – a bellwether sector of the economy – 
				is finally seeing some good times after two long arduous years. 
				The Zubair Automotive Group (ZAG) for instance expects to close 
				2011 on a promising note. Moreover, this growth is not 
				restricted to consumer vehicles, but is also apparent in the 
				sale of commercial vehicles. Says S Serdar Toktamis, group 
				general manager, ZAG, “There has certainly been a return of 
				confidence in the second quarter of the year and we have been 
				enjoying highly successful sales across the country in the ZAG. 
				There is a sense of more prosperity and a willingness to invest 
				in new cars. We are attracting new customers and existing 
				customers who are looking at upgrading to newer vehicles.” The 
				March 11, 2011 earthquake off the Pacific coast of Tohoku, Japan 
				led to a disruption in the supply of vehicles and this has 
				become a major handicap for distributors of Japanese brands like 
				Toyota, Mazda, Mitsubishi in catering to the sudden spurt in 
				demand.<br>
				<br>
				Realising the mood of the market the ZAG has launched a number 
				of new vehicles in the market – Dhofar Automotive launched the 
				Dodge Charger and Dodge Durango; Wattayah Motors the new Audi 
				A1, Audi A8 and Volkswagen Jetta, while European Motors has 
				brought in the Peugeot 3008 to the Sultanate. Says Toktamis, “It 
				is clear that the commitment of the group to bring fresh and 
				exciting models to the market is working and translating into 
				increased sales. Seeing the potential of the region principals 
				like Jeep, Dodge and Chrysler are renewing their focus on the 
				region.” The company is working on launching newer models in the 
				market later this year to maintain its momentum.<br>
				<br>
				<b><font size="2" color="#C00000">Retail game </font>
				<font color="#000080" size="2"><br>
				</font></b><br>
				<font color="#000080" size="7"><b>
				<img border="0" src="images/cover4-sep2011.jpg" width="150" height="160" align="left" style="border: 2px solid #C0C0C0"></b></font>The 
				upbeat mood is not just restricted to automobile sales but holds 
				true for other sectors too. In the consumer durable space, SARCO 
				(Samsung) has seen a 40 per cent jump in sales during the first 
				half of 2011. Sensing an opportunity in the market, the company 
				has taken a number of measures to capitalise on this growing 
				demand. In an effort to educate consumers better about their 
				products, Samsung started doing road shows on a bigger scale. 
				When the company launched Smart LED TVs in April 2011, it put up 
				three to four Smart LED models in Muscat City Centre with live 
				demonstrations. It has also been stressing on giving better 
				training its staff. Says Ajay Ganti, CEO, SARCO, “Earlier we 
				used to do staff training once in a month, but now we do it once 
				every week. In addition, simulation exercises are carried out in 
				which we ask our staff anticipated questions from consumers.”<br>
				<br>
				Having grown in excess of 50 per cent in Sohar and Nizwa in the 
				last year, SARCO is looking at strengthening its presence in the 
				interiors. It has set up a service and warehousing centre in 
				Sohar. A service centre in Nizwa is on the cards and the company 
				is looking at establishing more sale outlets in areas like 
				Buraimi, Salalah, Sur and Duqm. The company has also started 
				doing locally oriented campaigns. One big factor helping sales 
				in the consumer durable space has been an across the board fall 
				in the prices. A Pentium notebook with a dual core processor 
				which used to cost RO300-400 in 2008 now costs around RO200; a 
				52 inch LCD TV was priced at RO2,500 two years ago, now one can 
				get a 55 inch LED TV for just over RO1,000; smart phones prices 
				have fallen from an average of RO300 to RO70-80. Says Ganti, 
				“Holding onto your old product does not give you an advantage 
				and the cost of acquisition of new technology is no longer 
				prohibitive. As a result things which were perceived as a luxury 
				are now seen as a necessity.”<br>
				<br>
				<font color="#C00000" size="2"><b>Wheels of finance</b></font><br>
				<br>
				<font color="#000080" size="7"><b>
				<img border="0" src="images/cover5-sep2011.jpg" width="150" height="163" align="left" style="border: 2px solid #C0C0C0"></b></font>The 
				banking industry mirrors the state of the economy and it has 
				posted strong numbers during H1 2011. According to a GBCM report 
				titled – ‘H1 2011 Earnings – Review of MSM 30 Index 
				Constituents’ – ‘The performance of the banking sector was above 
				our estimates with the sharper growth in credit book, 
				maintenance of higher margins and the presence of higher 
				recoveries. Overall credit addition for the banking sector 
				during H1’2011 is at RO726.1mn. The sector’s credit growth is 
				about 6.8 per cent year to date (YTD), which is higher as 
				compared to our full year estimates of eight per cent. We saw a 
				strong revival in the banking sector’s credit off take during 
				Q2’2011 with an addition of RO474.4mn – the highest we have seen 
				in the last two years, which is commendable.”<br>
				<br>
				On individual performances, the GBCM report states, ‘The banking 
				sector major, BankMuscat reported robust earnings growth for the 
				quarter led by higher than anticipated book addition, 
				maintenance of margins and incremental recoveries. BankMuscat 
				added about RO271mn to its loan book for H1’2011, an increase of 
				6.5 per cent on an YTD basis. <br>
				<br>
				National Bank of Oman (NBO) surprised the market with the robust 
				credit off-take for the last quarter led by demand from 
				quasi-government projects and personal lending. The bank also 
				saw a steady pace of recoveries which led to improved earnings. 
				Ahli Bank came out with above than expected numbers driven by 
				improved growth and margins. Bank Sohar continued to show higher 
				numbers for the second consecutive quarter led by improved 
				margins and stabilisation of asset quality levels.” Says 
				AbdulAziz al Balushi, CEO, Ahli Bank “We are building a strong 
				institution without compromising on the quality of our assets. 
				As a prudent bank we do not have more than 13 per cent exposure 
				to any individual sector and in riskier sectors this is as low 
				as 5 per cent.”<br>
				<br>
				<b><font size="2" color="#C00000">Fuelling the economy</font><font color="#000080" size="2"><br>
				</font></b><br>
				<font color="#000080" size="7"><b>
				<img border="0" src="images/cover2-sep2011.jpg" width="150" height="165" align="left" style="border: 2px solid #C0C0C0"></b></font>Says 
				Adil Ismail al Raisi, managing director, Shell Oman Marketing 
				Company (SOMC), “There is a direct correlation between fuel 
				consumption and the state of the economy. The execution of more 
				infrastructure projects in the country has led to more demand 
				for petrol, diesel and lubricants.” Confident about its future 
				prospects SOMC has embarked upon a five-year development plan 
				which looks at improving the people, process and system 
				readiness of the company. The company is also stressing on HSE 
				issues, expanding its retail network and improving its service 
				delivery standards. Says Adil, “In SOM we always strive for 
				service excellence associated with full compliance with our HSSE 
				guidelines. We monitor our customer service and HSSE performance 
				through internal process and external parties aiming for 
				continuous improvement.”<br>
				<br>
				Like fuel retailing companies, the fortunes of the leasing 
				industry is also a good barometer of retail and industrial 
				activity. For example, the asset size of AOFS has grown from 
				RO125mn in June 2010 to RO142mn in June 2011 – a YoY growth of 
				14.5 per cent. Says Patel, “A large part of this increase came 
				from trade finance and not from asset finance as the company has 
				witnessed steady growth on both the consumption and corporate 
				side.” AOFS started corporate finance in 2006-07 and it now 
				accounts for almost 65 per cent of the company’s portfolio.<br>
				<br>
				<font color="#C00000" size="2"><b>Industrious performance</b></font>
				<br>
				<br>
				<font color="#000080" size="7"><b>
				<img border="0" src="images/cover6-sep2011.jpg" width="150" height="164" align="left" style="border: 2px solid #C0C0C0"></b></font>Commenting 
				on the performance of the industry sector, the GBCM report 
				states: “During H1’2011, the industry sector companies reported 
				improved revenue growth led by higher than expected performance 
				from commodity majors, consumer goods (food products) and 
				diversified industries. Al Jazeera Steel Products Company (AJSP) 
				and Oman Cables Industry (OCI) came out with stronger topline 
				growth on the back of higher commodity prices and improved 
				demand especially in steel products. Al Anwar Ceramics and 
				Voltamp Energy continued its topline growth owing to the 
				stronger domestic demand and the local flavour. A’Saffa Foods 
				and Dhofar Cattle Feed continued their topline growth which 
				clearly shows the robust local presence and the promotion of 
				local brands.” Says Bhaskar Dutta, CEO, AJSP, “The oil price 
				rise and definite increase in infrastructure and housing 
				spending in the GCC was a key factor for the increase in sales. 
				Reliability of supplies and the quality of our merchant products 
				helped us to replace imported products in the GCC market.”<br>
				<br>
				OCI has built upon the opportunities presented by the local 
				market. While earlier Oman used to contribute 35-40 per cent of 
				its revenues, it now makes up for 50 per cent of its sales. Says 
				Manoj M Vaidya, general manager (corporate finance), OCI, “In 
				Oman wherever cables are being used in projects in areas like 
				Duqm, Salalah, Sohar and Muscat our products are being used.”<br>
				<br>
				The company has also partnered with Takamul Investment Company 
				in establishing Oman Aluminium Processing Industries (OAPIL) in 
				Sohar. OAPIL which went on stream in 2010 has contributed RO15mn 
				in sales and RO200,000 in profits in H1’2011. Apart from the 
				regional market OCI has a strong presence in the Far East 
				markets and is looking at expanding its presence in Europe and 
				the North African markets. It has established an office in 
				Amsterdam and expects new business from Europe in the next few 
				quarters. Despite its good performance, Vaidya is quick to 
				strike a note of caution, “Being a cable company it is difficult 
				for us to give a sense of our operations on a quarterly basis. 
				Though it is a mandatory requirement, it does not establish a 
				trend.”<br>
				<br>
				<b><font size="2" color="#C00000">Prevailing hurdles</font><font color="#000080" size="2">
				</font></b><br>
				<br>
				<font color="#000080" size="7"><b>
				<img border="0" src="images/cover3-sep2011.jpg" width="150" height="154" align="left" style="border: 2px solid #C0C0C0"></b></font>Despite 
				the good tidings, there are certain concerns that continue to 
				afflict the corporate sector. Though most companies experienced 
				good topline growth, margins remained under pressure. The net 
				profit of MSM 30 companies during H1’2011 stood at RO215.60mn – 
				a decline of 19.7 per cent on a YoY basis. The net profit of the 
				industrial sector was the worst hit reporting a decline of about 
				38.9 per cent on a YoY basis.<br>
				<br>
				The volatility of commodity prices remains a major bug bear for 
				companies like OCI. In the last one and a half years the price 
				of copper has moved from $7,500 a tonne to $10,000 a tonne and 
				back to $9,000 levels. Volatility in copper prices scares 
				clients into holding back orders. “Every time we get used to a 
				new normal, the price changes creating disruption,” adds an 
				exasperated Vaidya.<br>
				<br>
				A recent Ministry of Transport decision that permits only 
				vehicles having either an Omani registration or the country of 
				delivery registration to carry goods has created bottlenecks for 
				the manufacturing sector. Dutta points out, “We are finding it 
				difficult to send our finished goods to Bahrain, Qatar, KSA and 
				Kuwait as local transporters cannot supply us with sufficient 
				vehicles. This has led to a piling up of dispatches and could 
				lead to a stoppage of the plant. Furthermore, the available 
				vehicles are being priced at abnormally high rates.”<br>
				<br>
				The sudden creation of jobs for Omanis is creating an attrition 
				problem for various companies. “In our industry where working 
				conditions are a little rugged, we find a number of workers 
				joining and leaving after a few days,” says Dutta. There are 
				others who voice concerns about productivity. AbdulAziz says, 
				“We are not worried about the cost of creating employment, but 
				productivity is the issue, employees should not feel that they 
				will be rewarded even if they do not work.”<br>
				<br>
				There are some apprehensions that the additional social outlay 
				may impact the government’s infrastructure spending. Says Patel, 
				“Given the additional budgetary outlay of RO1bn, there is a 
				shift from development to consumption expenditure. The number of 
				tenders awarded during H1’2011 have been lower than the 
				corresponding period during 2010 and that needs to go up in the 
				next six months.” Adds Kapadiya, “Confidence in the economy is 
				driven by government expenditure and so it needs to continue 
				spending. Once the private sector starts spending we will see a 
				marked increase in buoyancy.”<br>
				<br>
				During his presentation on the revised budgetary estimates, HE 
				Darwish tried to allay apprehensions about a cut in 
				infrastructure spending by reiterating the government’s 
				commitment to execute all projects that were approved for the 
				8th Plan (2011-15). Out of the RO12bn allocated for 
				infrastructure development in the 8th plan, RO6.4bn is earmarked 
				for ongoing projects launched during the 7th Five Year plan and 
				the remaining RO5.6bn will go to fund new projects. Says 
				AbdulAziz, “Whenever a project is announced there is a lead time 
				of six to eight months before the money gets sanctioned. Once 
				this happens business confidence will improve further.”<br>
				<br>
				The credit crisis in the Eurozone and the downgrading of the US 
				economy by Standard&amp;Poor’s is another area of concern but 
				industry leaders are quick to underplay its effect on the local 
				economy. “Since we live in a global village and we cannot be 
				completely insulated from the impact of global events, but the 
				fallout this time around will not be as bad as the last time,” 
				says AbdulAziz.<br>
				<br>
				<font color="#C00000" size="2"><b>Looking ahead</b></font><br>
				<br>
				<font color="#000080" size="7"><b>
				<img border="0" src="images/cover7-sep2011.jpg" width="150" height="158" align="left" style="border: 2px solid #C0C0C0"></b></font>The 
				GBCM report strikes an upbeat mood for the coming months – ‘We 
				expect corporate earnings to improve during the upcoming 
				quarters (especially in H2’2011) mainly driven by the local 
				consumption story coming in from the creation of about 70,000 
				jobs in the market. We also believe that companies have already 
				seen the full impact on the additional costs during Q2 and we 
				anticipate Q3’2011 earnings to improve sequentially on a lower 
				base.’<br>
				<br>
				The confident mood is shared by others. Says AbdulRazak, “The 
				Sultanate’s economic outlook for 2011 is positive stemming from 
				key factors like higher average price earnings from oil and 
				increased government spending on infrastructure. The economic 
				diversification programme is on track, backed by a resilient 
				banking system, stable exchange rate, sufficient foreign 
