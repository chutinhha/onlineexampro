						<a name="TYERS">Mentoring makes a difference</a></strong></span><br>
&nbsp;<p class="style15">
				<font face="Verdana" size="1"><span class="style14">
				<strong>Mentoring is fast becoming popular due to its potential 
				as an effective way of helping people to progress in their 
				careers is realised<br>
&nbsp;</strong></span></font><p class="style15">
				<b><font size="7">A</font></b>ll successful organisations create 
				a culture where the acquisition, development, <b><font size="7">
				<img border="0" src="images/edu-sep2011.jpg" width="200" height="187" align="right" style="border-style:solid; border-color:#C0C0C0; "></font></b>implementation, 
				transfer of skills and knowledge are highly valued. This type of 
				culture simply cannot exist where the practice of mentoring is 
				not a top down initiative.<br>
				<br>
				Mentoring is a powerful personal development and empowerment 
				tool. It is an effective way of helping people to progress in 
				their careers and is becoming increasingly popular as its 
				potential is realised. It is a partnership between two or more 
				employees (mentor and mentee) normally working in a similar 
				field or sharing similar experiences. It is a helpful 
				relationship based upon mutual trust, respect and creates a 
				culture of gratitudeness towards the management and the company.<br>
				<br>
				<b>Mentoring efforts can be done on the following five simple 
				steps:<br>
				</b><br>
				<b>Trust: </b>Any relationship between mentor and mentee that is 
				not built upon a foundation of mutual trust and respect won’t be 
				productive and won’t last long.<br>
				<br>
				Mentoring requires mutual commitment: Mentoring exercise 
				requires mutual commitment (from mentor and mentee) which 
				involves empathetic approach and flexibility towards the whole 
				process.<br>
				<br>
				<b>Walk the talk:</b> Mentoring is all about communicating how 
				much you believe in the process of being mentored by telling 
				your mentee how you have benefitted from your mentors, past and 
				present.<br>
				<br>
				<b>Choosing your mentees:</b> A person who won’t invest in 
				himself is probably not the right candidate for the long run. 
				Choosing a mentee will also depend upon the passion the mentee 
				has for that profession and the number of years spent in that 
				field. More important is the attitude to provide selfless 
				service to the organisation.<br>
				<br>
				<b>Ownership:</b> Effective mentoring programmes while led from 
				the top down are decentralised and driven down to lowest 
				possible levels of the organisation. Not everyone may be a good 
				choice for you to personally mentor, but if a person is worthy 
				of being part of the organisation and who holds ownership 
				towards his / her role can be the possible candidate for the 
				future of the company’s growth.<br>
				<br>
				<font color="#C00000" size="2"><b>High quality training</b></font><br>
				<br>
				The BizPro division of National Training Institute (NTI) offers 
				training consultancy that specialises in high quality training 
				solutions and custom designed training programmes to motivate, 
				enhance and generate the best from within an organization.<br>
				<br>
				The BizPro training programmes are geared towards helping the 
				employees understand and connect with their essence. This 
				enables them to create enriching and rewarding work-life 
				experiences. <br>
				<br>
				The local market requirements combined with international 
				standards in the design of BizPro suite of training programmes, 
				aims at enhancing the business skills of individuals that 
				complement their technical/academic qualifications, so that they 
				can generate a rewarding atmosphere within the workplace, which 
				helps them achieve organisational goals with integrity. The 
				training programmes are designed to take the participants 
				through the various stages of inner development, via 
				experiential learning, step-by-step guidance and <br>
				motivational exercises.<br>
