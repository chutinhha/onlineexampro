						<td valign="top">
						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">
						Nowhere to run</font></b><p align="justify" class="style13">
				<b>
				<font face="Verdana" size="7">
				<img border="0" src="images/stokmarket1-sep2011.jpg" style="border:2px solid #C0C0C0; " class="style19" align="right" width="150" height="171"></font><span style="font-size: 9pt">Following 
				the downgrading of the US credit ratings by S&amp;P, stock markets 
				went into a tizzy. The impact was felt in Oman as well. Tough 
				times are ahead, says <font color="#808080"><i>Abdullah Al Salmi</i></font>, 
				Executive Vice President, Capital Market Authority in a candid 
				chat with Visvas Paul D Karra</span></b><br>
				<br>
				Up until June, equity investors could count on the US Federal 
				Reserve to keep pumping money into the system, boosting equity 
				and commodity prices. The $600bn the Fed used to buy assets in a 
				second round of quantitative easing – known as QE2 – flooded 
				markets with cash and helped lower interest rates. That’s over 
				now as global policymakers realised that the twin debt crises in 
				Europe and the US are causing market turmoil and stoking fears 
				of the rich world sliding back <br>
				into recession.<br>
				<br>
				After the ‘Black Friday’ of August 5, saw more than $2.5trn 
				wiped off global stock markets, political leaders are under 
				mounting pressure to reassure investors that Western governments 
				have both the will and ability to reduce their huge and growing 
				public debt loads.<br>
				<br>
				The moves were part of a global response made additionally 
				necessary by Standard &amp; Poor’s (S&amp;P) taking the historic step of 
				cutting its US credit rating to AA+ from the top notch triple A 
				on that fateful Friday. As Europe struggles with its problems, 
				global markets also want to know how Washington will reduce its 
				more than $14trn debt without choking off economic recovery 
				since a modest US debt deal left Congress bruised and bitter.<br>
				<br>
				Even though the trigger for the crash in the financial markets 
				is the S&amp;P downgrade of US ratings, the downgrade per se is 
				probably a signal that the situation is far more grim than what 
				appears on the surface, says Abdullah Al Salmi, executive vice 
				president, Capital Market Authority. According to Abdullah, a 
				professional setup like S&amp;P will undertake this drastic step 
				only based on facts which most of us would not be aware of. If 
				that is the case of the shape of the things to come, it will 
				surely reflect on the global economy. Alongside what is 
				happening in Europe, together that does not give a bright 
				picture.<br>
				<br>
				Should S&amp;P downgrade other countries as well, that is another 
				question, says Abdullah, in that case, there are fears that even 
				countries like France or Japan could also be downgraded. That 
				will surely compound problems.<br>
				<br>
				<font color="#C00000" size="2"><b>Ironical twist</b></font><br>
				<br>
				What happened during the 2008-09 financial crisis was at a 
				micro-level wherein large financial institutions like the Lehman 
				Brothers and other big names on both sides of the Atlantic went 
				bankrupt. At that time, the governments were able to intervene 
				and bail them out in order to keep up the momentum of the 
				economy. Now the situation is at a macro level. Many 
				governments, including that of the US, themselves have gone 
				bankrupt, so who will rescue or bail them out. That’s the irony 
				of the situation, points out Abdullah.<br>
				<br>
				“Back then, the governments were able to spend in order to 
				stimulate the market, but now take the case of the US 
				government. You know, it cannot spend unless it has budgetary 
				allocations and if they have to reduce the deficit, they have to 
				increase taxes, for example, and that is another burden on the 
				economy. So it is really a complicated situation for the US. If 
				that is happening in the US which is the number one market in 
				the world it will definitely affect markets around the world 
				especially countries like China and other economies,” Abdullah 
				explains.<br>
				<br>
				In fact, for countries like China, it is a double whammy. 
				China’s exports will be hit as it is a major exporter to the US 
				and secondly because it is the biggest lender to the US. So this 
				is a vicious cycle.<br>
				<br>
				<font color="#C00000" size="2"><b>Wall Street sneeze!</b></font><br>
				<br>
				So this confirms that a Wall Street sneeze means the rest of the 
				world markets catch a cold. That is for sure but not in the way 
				you put it, Abdullah says. Since US is the major market in the 
				whole world, anything that happens there, consequentially 
				affects the rest of the world. But if S&amp;P has done the downgrade 
				then surely they are working on certain facts and if these 
				niggling issues are really existing there then the downgrade had 
				to occur. So yes, that is one side. Then looking into the US as 
				a whole, it is a major financial player and a major importer so 
				whatever happens there it will affect the others.<br>
				<br>
				“Having said that and looking into the matter locally, I think 
				the issue to a large extent is sentimental rather than anything 
				else. Unless what is happening around the world affects the oil 
				prices at the end of the day and if oil prices drop 
				significantly, Oman will be affected. But that stage has not 
				come as yet. So I don’t understand why our market is tumbling. 
				Most companies are doing well and the government spending is 
				well on track. And there are no signs that the government will 
				change its spending behaviour, but at the end of the day we are 
				part of the global village. Our main revenues are from oil 
				exports (the six nations of the Gulf Cooperation Council (GCC) 
				including Oman supply about a fifth of the world’s oil), so if 
				anything happens to the oil prices then it will have a cascading 
				effect. I think that is the reason the markets are going down,” 
				Abdullah says.<br>
				<br>
				Indeed, in the week beginning August 8, oil for September 
				delivery tumbled 9.2 per cent to $86.88 a barrel on the New York 
				Mercantile Exchange. And the US and European shares posted their 
				biggest weekly losses since November 2008 with the S&amp;P 500 
				plunging 7.2 per cent and the STOXX 600 Europe Index tumbling 
				9.9 per cent. Gulf stock markets, including the Muscat 
				Securities Market (MSM), dropped as news of the historic US 
				credit downgrading sent jitters through the region’s markets.<br>
				<br>
				<b><font size="2" color="#C00000">Crisis of confidence</font></b><br>
				<br>
				Confidence comes from facts and numbers. After witnessing what 
				is happening in the Eurozone and US, people need to see 
				something on the ground to regain confidence. “Unless some 
				positive signals come from the European Union that they are 
				going to come up with some solutions to the Eurozone debts, I 
				don’t think that that confidence will be back anytime soon,” 
				Abdullah asserts.<br>
				<br>
				Finance ministers and central bankers from the G20 
				industrialised and emerging economies pledged to ‘take all 
				necessary initiatives in a coordinated way to support financial 
				stability and to foster stronger economic growth in a spirit of 
				cooperation and confidence’. The European Central Bank (ECB) was 
				the only European institution capable of acting fast but 
				economists have warned that even the long-awaited ECB 
				intervention was no ‘silver bullet’ and that big obstacles 
				remained in stabilising strained public finances and putting 
				adequate eurozone defence mechanisms in place quickly.<br>
				<br>
				According to Abdullah, the situation is slightly better in the 
				US than in the European Union (EU), because in the US, there is 
				one political power dominating the government but in the EU 
				although you have a common currency, there are 10 different 
				political entities with different agendas. To bring all these 
				agendas on to the table for a unified strategy is very 
				difficult. So the events in the EU and US, will affect other 
				markets by default. And that is where the crisis of confidence 
				stems from, says Abdullah. No other market can substitute US and 
				Europe, he adds.<br>
				<br>
				<font color="#C00000" size="2"><b>Future trend</b></font><br>
				<br>
				The million dollar question on everybody’s lips is ‘what next?’. 
				I wish I knew, says Abdullah with a smile. Nobody was expecting 
				the downgrade because the US government did manage to avert a 
				sovereign default by lifting its own debt ceiling. Even before 
				the US ratings downgrade, the sluggishness in economic growth of 
				various countries was endemic. If you look at the industrial 
				output, there was not so much as a shakeup. In the US, it was 
				around 9 per cent – not higher than during the financial crisis. 
				But now the US government needs help so it will take some time 
				to adjust its monetary or fiscal policies and that is not clear 
				to the investors and the markets, says Abdullah.<br>
				<br>
				As for Oman, during the earlier financial crisis, the Sultanate 
				had strong fundamentals which helped the country. So it truly is 
				a lack of confidence. “I think the funds like Pension Funds 
				should invest in the market, if they think the fundamentals are 
				right and if the pricing is attractive. This is not for the sake 
				of bailing out the MSM but in order to make some money in the 
				future,” Abdullah opines.<br>
				<br>
				Continuing further, he says, more institutional investors should 
				come forward because individual investors are not rationale or 
				knowledgeable enough. But institutional investors have a 
				knowledge of how the markets will behave. So if they come 
				forward and invest, it will give the rest of the investor’s 
				confidence to come back to the market. Most of the institutional 
				investors have said that the fundamentals are right and the 
				market price is good. While going to press the US downgrade and 
				fears of the Euro zone debt crisis was still playing out across 
				world markets. This is the last thing that the global economy 
				needed at this juncture, but shortsighted politics and 
				brinksmanship has landed the world in the throes of a crisis, 
				yet again.
				<br>
