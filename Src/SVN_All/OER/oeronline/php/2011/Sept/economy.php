						<p align="justify" style="margin: 3px 10px">
						<span class="style27"><strong>Energy boost</strong></span><p class="style15">
				<strong><span class="style14">How is the Sultanate planning to 
				meet its growing utilities need in the short term and long term?<br>
				<br>
				<font color="#808080"><i>By Oliver Cornock</i></font><br>
&nbsp;</span></strong><p align="center" class="style13">
				<img border="0" src="images/economy-sep2011.jpg" width="450" height="251" style="border: 1px solid #C0C0C0"><p align="justify" class="style13">
						<br>Stay cool, not cold. Set to 23.” That is the advice 
						of Noor, the Arabian Oryx who encourages passing Omanis 
						to raise their thermostats to 23º during the summer 
						months. Last year, the Save Energy Oman campaign used 
						billboards starring Noor as part of a broader effort to 
						encourage energy conservation and prevent possible 
						energy shortages in the Sultanate.<br>
						<b><font size="2" color="#C00000"><br>
						Energy shortages?</font></b><br>
						<br>
						Indeed, an energy conservation campaign may at first 
						seem out of place in Oman, an oil and gas exporter. 
						Although the Gulf nation’s hydrocarbon supplies are not 
						as large as some of its neighbours’, its hydrocarbon 
						resources are considerable. In fact, much of the 
						Sultanate’s energy comes from natural gas-fired power 
						plants, which burn the majority of the country’s gas 
						production. But in addition to powering its own country, 
						Oman understandably wants to maximise the revenues from 
						its resources and has committed a significant amount of 
						its gas to international concessions some years ago. 
						However, this was when domestic energy demand was far 
						lower, and those days are long gone. According to a 
						statement by the executive director of Oman’s Authority 
						for Electricity Regulation, John Cunneen, Oman hit 
						record-breaking demand levels recently, peaking at 3,900 
						MW on May 21, 2011. With a maximum installed capacity of 
						3,700 MW, utilities providers turned to reserve diesel 
						generators to meet energy needs. According to industry 
						sources, last month’s energy demand is not a fluke but 
						part of a predicted long-term growth. At a conference in 
						Muscat last May, the chairman of the public authority 
						for electricity and water HE Mohammad Abdullah al 
						Mahrouqi, predicted demand would increase by nine per 
						cent annually in coming years.<br>
						<br>
						“[The Sultanate’s recent economic] growth has been 
						combined with a substantial increase in demand for both 
						electricity and water,” he says.<br>
						<br>
						<b><font color="#C00000" size="2">Increasing appetite 
						for energy</font></b><br>
						<br>
						There are a number of factors underlying the Sultanate’s 
						increasing appetite for energy. One is Oman’s ongoing 
						industrial growth. As outlined in Vision 2020, the 
						government’s economic diversification plan launched in 
						the mid-90s, the government views industrial development 
						as a top priority.<br>
						<br>
						One area that has been gaining traction lately is 
						aluminium. Demand for this high strength and light 
						weight metal has blossomed in recent years. Demand in 
						2010 grew 13 per cent and is set to double by 2020, 
						according to the Klaus Kleinfled, CEO of international 
						aluminium giant Alcoa. Oman and its GCC neighbours are 
						well placed to become major producers. Manufacturing 
						aluminium is an energy intensive-process, with 
						electricity making up as much as 30 per cent of costs – 
						indeed, access to readily available sources of 
						competitively-priced energy is one of the competitive 
						advantages the Gulf region holds for the industry. The 
						Sultanate’s Sohar Aluminium announced last April that it 
						plans to invest $3bn to double its 370,000 tonne annual 
						output. According to Sohar’s CEO, Henk Pauw, one major 
						challenge for the company will be securing energy 
						supplies to fuel the expanded plant.<br>
						<br>
						Other industries, like petro-chemicals and steel, also 
						require significant energy input and expansion plans are 
						afoot elsewhere. For example, both Brazilian firm Vale 
						and Indian firm Jindal are investing to boost the 
						Sultanate’s steel production capacity.<br>
						<br>
						As industrial ventures continue to expand in the 
						Sultanate, energy demand is set to grow with them. In 
						order to ensure that Oman’s gas resources are 
						distributed efficiently, the government has initiated a 
						system of gas allocation for industrial projects. The 
						authorities aim to ensure that a wide variety of sectors 
						are able to develop. In pursuit of this goal, criteria 
						have been set for gas allocation. Preference is to be 
						given to industries that show the greatest potential for 
						creating jobs as well as downstream industries, such as 
						aluminium processing plants that use raw metal to make 
						finished products like cables and wires.<br>
						<br>
						In addition to allocating current energy supplies, the 
						authorities are also seeking ways to increase Oman’s 
						overall output. One possibility is getting more out of 
						Oman’s current gas reserves. Many of the Sultanate’s 
						deposits, like the Khulud field discovered in 2009, are 
						deep and hard to extract. In industry terminology, this 
						type of gas is termed unconventional.<br>
						<br>
						There are a number of methods for unconventional gas 
						extraction. One called “fracking” involves the use of 
						hydraulic pumps to fracture underground rock formations 
						and release gas deposits within. These innovations do, 
						however, come at a premium, resulting in unconventional 
						gas’ somewhat slimmer profit margins compared to 
						conventional supplies. As long as energy prices remain 
						high, however, the margins when using these complex 
						extraction methods are viable.<br>
						<br>
						<b><font size="2" color="#C00000">Diversifying energy 
						resources</font></b><br>
						<br>
						Although gas provides the lion’s share of electricity in 
						the country, Oman – along with its GCC neighbours – is 
						also looking into ways to diversify its energy 
						resources. In both the United Arab Emirates (UAE) and 
						Saudi Arabia, governments are planning to build nuclear 
						power plants to cope with demand growth projections. 
						Last March the director general of Abu Dhabi’s Water and 
						Electricity Authority announced that the Emirate would 
						go ahead with plans to build and operate a nuclear 
						reactor by 2017. In Saudi Arabia as well, the government 
						announced a coming agreement with Areva, a French 
						nuclear firm, to construct nuclear and solar plants in 
						the Kingdom.<br>
						<br>
						To that end, Oman has also been exploring alternatives 
						to its gas-fired power plants. Government ministers are 
						currently reviewing proposals for a 200 MW solar 
						project, Charles Rackham, director of projects 
						development at the Oman Power and Water Procurement 
						Company, said at the recently held Oman Power and Water 
						Summit event. In addition to boosting its own output, 
						the Sultanate is also connecting with nearby countries 
						to facilitate more energy import opportunities. Last 
						May, Oman and the UAE agreed on the construction of a 
						link that can transfer up to 200 MW of electricity 
						between the two countries. In the longer term, a GCC 
						power grid is set to connect Oman’s grid to a regional 
						electricity infrastructure. Such a network is likely to 
						be important in the future because, like Oman, all the 
						Gulf countries are set to experience continuing 
						increased demand for energy.<p align="justify" class="style13">
