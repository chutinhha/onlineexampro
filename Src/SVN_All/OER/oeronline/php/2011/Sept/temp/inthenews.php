				<b><font size="2" color="#008000">A ringside view</font><font color="#0000FF"><br>
&nbsp;</font></b><p align="center" class="style13">
				<img border="0" src="images/inthenews2.jpg" style="border: 1px solid #808080" width="450" height="274"><p align="justify" class="style13">
				<br>
				<b><span style="font-size: 9pt">Oman Power &amp; Water Summit 
				highlights developments taking place in the Sultanate’s 
				utilities sector. An OER report<br>
				</span></b><br>
				<br>
				The first-ever Oman Power &amp; Water Summit held between May 22-25, 
				2011 put the spotlight on ongoing and upcoming projects to keep 
				the Sultanate’s power and water supply in pace with growing 
				demand as the country continues to experience rapid industrial 
				and economic development.<br>
				<br>
				The four-day event was held under the patronage of the Public 
				Authority for Electricity and Water (PAEW) and was officially 
				inaugurated by PAEW chairman HE Mohammed Abdullah Al-Mahrouqi. 
				The formal opening ceremony was attended by ministry officials, 
				diplomats, the top executives of companies, industry experts and 
				over 250 delegates representing many leading players in the 
				region’s power and water sector.<br>
				Organised by Global Exhibitions &amp; Conferences (GEC) and IQPC 
				Middle East, the Oman Power &amp; Water Summit was officially 
				supported by the Ministry of Regional Municipalities and Water 
				Resources (MRMWR), Electricity Holding Company (EHC), Oman Power 
				and Water Procurement Company (OPWP), Rural Areas Electricity 
				Company (RAECO) and the Authority for Electricity Regulation, 
				Oman (AER).<br>
				<br>
				“The Oman Power &amp; Water Summit is truly a high-profile industry 
				event with the strong presence of government authorities, public 
				and private sector companies, industry professionals and other 
				participants, all of whom play a major role in the continued 
				development of the sultanate’s vibrant power and water sector,” 
				said CJ Paul, CEO of GEC.<br>
				The event was supported by Siemens as the gold sponsor and ACWA 
				Power International and Wartsila as the silver sponsors. GE was 
				the event guide sponsor, while the associate sponsors included 
				ABB Transmission and Distribution, DYK Incorporated, Future Pipe 
				Industries, Greenergy, HBK Contracting, Khimji Rhamdas, and 
				Solar Total. The summit also had the full backing of the Oman 
				Society of Contractors, Oman Water Society, the London-based 
				Chartered Institution of Building Services Engineers (CIBSE) and 
				the Society of Public Health Engineers (SoPHE).<br>
				<br>
				The Oman Power &amp; Water Summit featured technical presentations, 
				panel discussions, case studies, roundtable sessions and 
				interactive workshops. The event’s conference programme covered 
				an extensive range of topics, including the potential of 
				renewable energy technologies in Oman, the government’s future 
				plans for the development of the country’s power and water 
				sector, sustainable management of the sultanate’s water 
				resources, strategies for improving power system capacity and 
				efficiency, and the importance of private sector participation 
				and investment in the industry.<br>
				<br>
				“The speakers chosen to discuss these topics are renowned 
				industry experts and highly influential personalities whose 
				vision and future decisions will be the main driving factors in 
				sustaining the industry continued growth,” said Rozenn Cornec, 
				conference director at IQPC Middle East. “Delegates of the Oman 
				Power &amp; Water Summit got the opportunity to not only be updated 
				on the latest developments in the industry but also got a full 
				sense of the range of prospects in this highly important 
				sector.”<br>
				<br>
				On the sidelines of the conference there was an exhibition 
				featuring the display of all the corporate sponsors and other 
				major companies that are active in the power and water sector. 
				The exhibitors included Al Dastoor Contracting &amp; Trading, 
				Douglas OHI , KSB Middle East, Larsen &amp; Toubro Oman, 
				Saint-Gobain PAM, Septech Muscat, Sogex Oman Co. , Towell 
				Engineering and Target.<br>
				<br>
				The Oman Power and Water Summit was supported by leading media 
				companies, including the AEC Online, Alam al-Iktisaad Wal A’mal, 
				BNC Network, Mbendi Information Services, Middle East Tenders, 
				OER Dossier, Oman Economic Review, Oman Projects, Oxford 
				Business Group, Times of Oman, and Zawya.<br>
