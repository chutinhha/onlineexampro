						<span class="style27"><strong>A new beginning</strong></span><p class="style15">
				<span class="style14"><strong>Vale commences first phase of 
				operations of $1.356bn industrial complex in Sohar<i><br>
&nbsp;</i></strong></span><p class="style15" style="text-align: center">
				<font face="Verdana" size="1">
				<img border="0" src="images/inthenews1.jpg" style="border: 1px solid #808080" width="450" height="377"></font><p align="justify" class="style13">
				The Middle East is slated to become a world-class producer and 
				distributor of iron ore pellets with the start of phase one of 
				operations in Vale’s $1.356bn pelletising plant and distribution 
				centre in Oman. Located in the coastal city of Sohar, the 
				Industrial Complex will serve as a hub catering to the growing 
				demand of iron ore products in the Middle East, North Africa and 
				India (MENAI).<br>
				<br>
				Vale’s Industrial Complex comprises a two-unit pelletising 
				plant, each with a nominal capacity of 4.5mn metric tonnes of 
				direct reduction pellets per year, in addition to a distribution 
				centre with an annual capacity of 40mn metric tons. In May 2010, 
				Vale entered into a strategic partnership with the Omani 
				government through the sale of a 30 per cent stake in the 
				pelletising company to Oman Oil Company.<br>
				“Sohar’s strategic location outside the Strait of Hormuz, with 
				the advantage of deep water seas, and the heavy infrastructure 
				investments to provide leading logistics networks, advanced 
				energy and power supply technologies and world-class facilities 
				at Sohar Industrial Port, were key to our decision to establish 
				our operations in the Sultanate,” says Roger Agnelli, Vale CEO.<br>
				<br>
				The visionary economic diversification plan adopted by the Omani 
				government will enable the establishment of a competitive steel 
				cluster that will be a vital component contributing to the 
				reduction in the 30mn metric tonnes supply-demand gap of 
				finished steel in the region. Vale’s project in Oman will create 
				a just-in-time operations framework to serve customers better 
				across MENAI. The company is committed to emerge as the largest 
				investor in the mining industry operating around the world with 
				a simple yet dynamic premise that there can be no future without 
				mining and there is no mining without caring about the future.<br>
				<br>
				Vale’s history is marked by a capacity for transformation and 
				has played an instrumental role in building a solid industrial 
				platform by positively contributing to the development of the 
				local economy. In line with this strategy, the company 
				contracted Sohar Industrial Port Company to build a 1.5km jetty 
				to be exclusively used and operated by Vale, which is scheduled 
				to be operational in June 2011. Three ship-loaders and one 
				ship-un-loader have already been delivered. A long-term 
				agreement was also signed with Oman Shipping Company for the 
				construction of four very large iron ore carriers (VLOCs) with a 
				loading capacity of 400,000 tonnes to be dedicated to Vale.<br>
				<br>
				Driven by its commitment to the sustainable development of the 
				Sultanate, Vale places high priority on developing and retaining 
				local qualified talents in the communities where it operates. To 
				date, the company has invested $11.3mn in specialised 
				recruitment and training programmes that aim to generate 720 
				direct and 1,800 indirect jobs.<br>
				<br>
				Vale considers the environment as a fundamental component of its 
				operations and seeks to balance environmental protection with 
				economic development. In Oman, the company continually invests 
				in managing environmental aspects and risks as well as research 
				new technology to surpass the established standards of 
				excellence in environmental control systems. A total of $40mn 
				has been invested in green technology which includes an advanced 
				100 per cent water recycling system as well as a 3,150-mtr wind 
				fence surrounding the industrial complex to control dust 
				emissions. Vale is also innovating the technology used for the 
				production of pellets, making its plant in Sohar the first to 
				use ‘Grate Kiln’ furnace. This technology will provide 
				consistent quality results and an end product with high 
				compression, low abrasion and perfect roundness. Headquartered 
				in Brazil, Vale operates in 38 countries on five continents.<br>
