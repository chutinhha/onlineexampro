&nbsp;</font></b><p align="justify" class="style13">
				<img border="0" src="images/inthenews3.jpg" style="border: 1px solid #808080" align="left" width="200" height="435">For 
				GUtech, sustainability is an important issue and plays a major 
				role in all its programmes. Sustainable development is 
				development that meets the economic, social and environmental 
				needs of the present without compromising the ability of future 
				generations to meet their own needs.<br>
				<br>
				After the success of last year’s Muscat Green Days Conference – 
				“Education for a Sustainably Built Environment” a second 
				conference on sustainable urban planning and sustainable tourism 
				issues will be held end of November this year at the Grand Hyatt 
				Muscat. GUtech university is keen on creating awareness and 
				promoting sustainable architecture and urban planning as well as 
				sustainable tourism in Oman - for the benefit of the economy of 
				the region, the environment and our future generations.<br>
				<br>
				The two-day conference will be organised by GUtech and Sesam 
				Consulting. Moreover, the university is an academic partner of 
				the conference. The chair for the conference is taken by Dr 
				Dieter Fuchs, general manager of the German Research Institute 
				Fraunhofer in the Middle East and President of the World 
				Association of Applied Research, WAITRO. <br>
				<br>
				During this year’s Muscat Green Days Conference a number of 
				GUtech projects, such as social housing projects, research on 
				sustainable urban planning patterns and the GUtech green campus 
				in Halban will be presented. <br>
				<br>
				The GUtech campus in Halban will be one of the first Green 
				building campuses in the Sultante. When the master plan was 
				developed, sustainability guidelines were worked out which 
				provide the basis for the design of single buildings. For 
				example, one detail is the green courtyards which are located in 
				front of all seminar and classrooms to create a comfortable 
				micro climate. Another aspect is the orientation of the 
				buildings considering the local climatic conditions such as 
				solar radiation, wind direction and others. Other green elements 
				are economising of natural resources, reuse of water and using 
				solar energy.<br>
