						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">
						Get rid of ambiguities</font></b><p class="style15">
				<strong><span class="style14">While drafting a contract, spell 
				out everything in requisite detail so that there is no room for 
				alternative views on what is meant and intended</span></strong><p class="style15">
				It is often worth looking at what causes two parties to be in a 
				dispute, especially in the current climate, where minimising 
				risk is all important. An obvious cause of dispute is when a 
				contract has terms in it which are not defined. Both parties may 
				think that the other party agrees on what is meant by certain 
				wording, but that is a big risk to take, especially as personnel 
				may change over the years, meaning that different people might 
				be administering the contract by the date when a dispute occurs.<br>
				<br>
				An obvious example is time-frame. A contract may say that 
				something has to be done within four weeks. But that begs the 
				question of which event triggers the beginning of the four week 
				period. Furthermore, if the starting date was Monday 28 March 
				2011, some people will say that four weeks means a deadline of 
				28 April 2011, whilst others will argue that the four weeks 
				elapse on the fourth Monday after March 28, 2011, ie on Monday, 
				April 25, 2011.<br>
				The critical factor is spelling out everything in requisite 
				detail so that there is no room for alternative views on what is 
				meant and intended. Indeed, it is often the case that diagrams, 
				drawings and maps give greater clarity than mere words. In such 
				instances, these pictorial representations should be annexed to 
				the contract.<br>
				<br>
				Similarly, it is often the case that both parties agree on a 
				somewhat intricate financial calculation by which to determine 
				the monetary amount owed by one party to the other party. In 
				these scenarios, we tend to advise that it is best to have a 
				schedule attached to the contract, giving a worked example with 
				numbers, so that there can be no later debate as to how the 
				monetary amount was going to be quantified.<br>
				<br>
				<b>Arbitration tangles<br>
				</b>Most contracts also have clauses about governing law and who 
				will hear any dispute. It is not uncommon to read sentences such 
				as: “Arbitration: The Oman Courts will arbitrate upon any 
				dispute.” This immediately causes confusion, as some people will 
				say that this means that the Oman Courts will hear the dispute, 
				whereas others will say that the sentence evidences an intention 
				by both parties to opt for arbitration as opposed to resolution 
				via the Courts.<br>
				<br>
				Another problem is that sometimes the contract does not specify 
				with exactitude who are the contractual parties. This is 
				especially important when there may be two or more affiliates 
				with almost identical names. To avoid confusion, it is best to 
				specify the address and CR number of each entity at the 
				beginning of the agreement.<br>
				<br>
				One rule of thumb is that a well-drafted contract should begin 
				with precise definitions of terms. The defining of key terms 
				negates the ability of either party to argue at a later date. In 
				addition, a good contract should be easy to read and very clear 
				as to what is meant and mutually intended by the parties. It is 
				important to explain in a contract as to why the parties are 
				agreeing to do something which perhaps is not entirely orthodox 
				or the conventional way of doing things.<br>
				<br>
				In the final analysis, parties to a contract need to keep in 
				mind that, if there is a dispute, it will be a third party 
				(court, expert, or arbitral panel) who will decide what was 
				meant by any ambiguous contractual wording. So it is wise to 
				keep in mind, when drafting a contract, as to how it would be 
				interpreted in later years by someone who is not necessarily 
				totally familiar with the industry in question. It also pays to 
				be a cynic when writing a contract - if you can envisage the 
				ways that the other party may try to circumvent the contractual 
				wording, you can draft the document in such a way as to 
				eliminate any loopholes!</td>
