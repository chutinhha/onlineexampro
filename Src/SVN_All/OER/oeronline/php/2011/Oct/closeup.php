						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">
						Competitiveness of GCC economies<br>
						&nbsp;</font></b><p align="justify" class="style13">
				<span class="style14"><strong>The Global Competitiveness Index (GCI) 
				rankings suggest all GCC countries have room to improve their 
				positions if attention is paid to education, training and 
				economic integration<br>
				<br>
				<font color="#808080"><i>Dr Jasim Husain Ali</i></font><br>
				</strong></span><br>
				The recently released Global Competitiveness Report 2011-2012 
				confirms continued success of at least some Gulf Cooperation 
				Council (GCC) economies in sustaining progress of 
				competitiveness of their global rankings. Qatar, Saudi Arabia, 
				Oman and Kuwait made headway in the rankings of their economies. 
				Bahrain maintained its earlier ranking. However, the UAE saw its 
				rankings plummet among the 142 economies ranked in the latest 
				report.<br>
				<br>
				<b><font size="2" color="#000080">Three categories</font></b><br>
				<br>
				The World Economic Forum (WEF) publishes the annual report which 
				ranks reviewed economies on the basis of their performance on 
				the Global Competitiveness Index (GCI). In reality, GCI is noted 
				for taking a comprehensive look into reviewed economies by 
				relying on a set of variables.<br>
				<br>
				More specifically, the index ranks economies on the basis of 
				their achievement on three broad categories, namely basic 
				requirements, efficiency enhancers and innovation and 
				sophistication factors. In turn, the basic requirements category 
				is subdivided into institutions, infrastructure, macroeconomic 
				stability, health and primary education. Still, the efficiency 
				enhancers category comprises higher education and training, 
				goods and market efficiency, labour market efficiency, financial 
				market sophistication, technological readiness and market size. 
				Yet the innovation and sophistication factors category is made 
				up of business sophistication and innovation.<br>
				<br>
				The three categories of basic requirements, efficiency enhancers 
				and innovation and sophistication factors are brought together 
				in the formation of GCI, which comprises a maximum seven 
				points.The index is developed by using publicly available data 
				and WEF’s own opinion surveys. Hundreds of business leaders are 
				polled for the purpose of gathering data for the report.<br>
				<br>
				<b><font size="2" color="#000080">Qatar on top</font></b><br>
				<br>
				The report ranks Qatar the 14th most competitive economy in the 
				world, the best within the GCC, Arab and Middle East and North 
				Africa region. The achievement partly reflects budgetary 
				surpluses, in turn allowing for the absence of debt, hence 
				macroeconomic stability.<br>
				<br>
				In fact, only last week, Qatari authorities increased the 
				salaries and allowances granted to nationals working in civil 
				and military establishments by 60 per cent and 120 per cent, 
				respectively. Undoubtedly, this increase adds to the wealth of 
				Qatari nationals, who interestingly enough are already regarded 
				as the wealthiest in the world. According to the IMF, per capita 
				income in Qatar stands at about $90,000, second to none.<br>
				<br>
				With its abundant oil and gas resources, Qatar has benefited 
				substantially from firm energy prices of the recent past. In 
				fact, Qatar is the largest exporter of liquefied natural gas 
				(LNG) in the world. Output capacity stands at 77 million tonnes. 
				Qatar managed to expand its gas business through joint 
				production agreements with international oil firms. It means 
				that Qatar is reaping fruits of exceptional economic policies.<br>
				<br>
				<b><font size="2" color="#000080">Saudi’s focus on education</font></b><br>
				<br>
				Of all GCC countries, Saudi Arabia made the biggest stride in 
				the competitiveness index, with its ranking rising by four 
				notches to No. 17 worldwide. The Saudi advancement partly 
				reflects government’s determination to spend on education and 
				training. Saudi Arabia’s ninth development plan, covering the 
				period 2010 to 2014, released last year, calls for developing 25 
				technical colleges and equipping many other institutes with 
				latest technologies. Other schemes include opening some 117 
				hospitals and a number of clinics.<br>
				<br>
				Saudi Arabia is ahead of other GCC countries in attracting 
				foreign direct investments (FDI), as demonstrated in the World 
				Investment Report 2011. In fact, Saudi Arabia’s FDI amounted to 
				around $28.1bn in 2010, which accounts for 48 per cent of the 
				total FDI inflows in the entire West Asia, which includes the 
				GCC countries, Turkey and Iran. Undoubtedly, this phenomenon 
				serves as testimony of the strength of Saudi Arabia’s economy. 
				The UAE was the only GCC country to suffer decline in the 
				ranking of its economy on the competitiveness index by two 
				notches to number 27 in 2011-2012 report. The development partly 
				reflects lingering problems related to Dubai’s debt problem, 
				originally uncovered in late 2009. Amongst other debacles, the 
				crisis has undermined competitiveness of Dubai’s economy 
				vis-à-vis other regional economies. <br>
				<br>
				<b><font size="2" color="#000080">Oman’s advance </font></b><br>
				<br>
				Oman saw competitiveness of its economy grow steadily by two 
				notches, clinching number 32 worldwide, indeed a record for the 
				Sultanate. Furthermore, the Sultanate stood out amongst other 
				GCC countries with a significant rise in its inward FDI in 2010, 
				as illustrated in Unctad’s latest report. Oman’s inward FDI grew 
				from around $1.5bn in 2009 to over $2bn in 2010. The positive 
				development, an exception compared to the rest of the countries 
				in the group, is owing to the growing appreciation of 
				international investors of the opportunities in Oman, a country 
				with immense potential in the tourism sector.<br>
				<br>
				Kuwait succeeded in advancing its ranking by a single notch to 
				number 34 globally reflecting absence of deficit threats thanks 
				to firm oil prices in international markets. Still, Bahrain 
				remained the least competitive GCC economy by virtue of its 
				ranking remaining unchanged at number 37 globally.<br>
				<br>
				<b><font size="2" color="#000080">Towards integration<br>
				</font></b><br>
				The GCI rankings suggest all GCC countries have room to improve 
				their positions with particular attention paid to education and 
				training and economic integration. In fact, a key drawback for 
				several GCC countries like Bahrain and to some extent Oman 
				relates to limited market size.<br>
				<br>
				Yet, integrating GCC economies via the Gulf Common Market (GCM) 
				scheme should serve the cause of all member countries. Commenced 
				in January 2008, the GCM calls for free flow of factors of 
				production amongst member states. In practice, the GCM focuses 
				on finding a unified regional market through which nationals can 
				benefit from opportunities available in regional economies.<br>
