				<br>
				The Eighth edition of OER CEO Golf will take place at Muscat 
				Hills Golf &amp; Country Club, Oman’s first PGA-certified green golf 
				course. Presented by Nawras Business Solutions, OER CEO Golf 
				will be held on January 12, 2011 with a promise to be bigger, 
				better and grander this time around thanks in part to the 
				beautiful settings provided by Muscat Hills.<br>
				<br>
				OER CEO Golf is a unique opportunity for the leaders of Oman’s 
				corporate community to link up over an 18-hole game of golf at 
				the country’s finest, and one of the Middle East’s most exciting 
				PGA-standard courses. For those new to the game it is a good 
				chance to get acquainted with the CEO sport. Golfers-to-be will 
				get an opportunity to get a quick induction to the game with 
				tips and tricks from an expert, followed by a trial game over a 
				few holes.<br>
				<br>
				The naturally undulating terrain of Muscat Hills looks like as 
				if nature has sculpted it exclusively with a golf course in 
				mind. The developers of the golf course built the course on this 
				natural settings. Built in its own distinct corner of Muscat’s 
				rugged hills and etched by canyon-like wadis, the eighteen holes 
				have been designed to complement all of the natural features, 
				while the lush green fairways and greens provide a startling 
				contrast to the rugged desert terrain.<br>
				<br>
				The wide green fairways offer some relief for the ‘errant’ tee 
				shot but to score consistently well, the generously sized greens 
				demand accurate approach shots from the correct position <br>
				on each hole.<br>
				<br>
