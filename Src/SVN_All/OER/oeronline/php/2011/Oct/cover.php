				representation all that more valuable. I feel that people are 
				more positive towards women candidates and are confident in the 
				representation of women and their capabilities.”<br>
				<br>
				<b><font size="2" color="#000080">Reaching out to people</font></b><br>
				<br>
				Most of the candidates are of the opinion that they have to 
				campaign aggressively if they want to win seats in the election 
				rather than waiting to acquire power on a silver platter. <br>
				<br>
				‘Shura is a responsibility; so let’s become partners in 
				shouldering it’ is the campaign motto of Dr Jokha bint Suleiman 
				Al Hosni, who is contesting from the Wilayat of Bowshar. Her 
				manifesto speaks about the necessity of reforming and upgrading 
				the educational system in the country in order to improve the 
				quality of graduates from colleges and universities across the 
				Sultanate. She also emphasises the development of small-scale 
				industries in achieving the targets of sustainable development. 
				Other areas of import in her agenda include infrastructure 
				development and making service sectors more efficient.<br>
				<br>
				Says Khadija, “At present, I’m meeting a lot of people in my 
				house to introduce myself, my objectives and electoral 
				programmes. I’m also making use of the electronic and internet 
				to campaign and reach people. In addition to that I’m also 
				visiting people in their communities to hear and discuss their 
				immediate worries and problems. People are very positive, and 
				they believe that women are more dedicated in their work and 
				know their problems better than men.”<br>
				<br>
				Friends and family members are active in the election campaign 
				of Suad. Her campaign includes face-to-face meeting with people, 
				putting up campaign posters, and communicating with people via 
				Facebook and other social media websites. “However certain 
				people harbour a grudge against putting up women’s photos in 
				public,” she says.<br>
				<br>
				Backed by 30 years of activities for social development and her 
				vast experience as a member of various national committees and 
				panels, Dr Aydah is very much confident about her plans for 
				people. She visits houses, meets people in order to convey to 
				them her dreams and aspiration for their development. Her 
				election manifesto comprises a lot of visionary projects for the 
				welfare of people and the development of the Wilayat. Shamsa 
				promises to her voters that once elected, she will work 
				diligently in association with the constituents for the 
				development of the Wilayat. She also plans to work for 
				facilitating more interaction among people in the locality and 
				to give voice to their dreams.<br>
				<br>
				<b><font size="2" color="#000080">Online campaigns</font></b><br>
				<br>
				<font color="#000080" size="7"><b>
				<img border="0" src="images/cover23-oct2011.jpg" width="150" height="151" align="left" style="border: 2px solid #C0C0C0"></b></font>Most of the women candidates are making use of advanced 
				technology to reach out to more voters. Al Yusriya who works at 
				the Ministry of Education, uses Ozv Specialist (Technology) and 
				various websites for her campaign. “I designed my own website 
				which helps me communicate with a large segment of society. It 
				provides a better opportunity for me to disseminate my vision 
				and objectives to more people,” she says. Yusriyah presents to 
				the public two comprehensive lists of her priorities which 
				includes her projects for low-income families, plans to upgrade 
				health and other public services, elimination of literacy, 
				measures for the development of Muttrah market etc.<br>
				<br>
				“The underlying message of all campaigns is to make people 
				believe that you can serve them as a servant. I am using all 
				social media websites such facebook and twitter for the 
				campaign, “says Aziza al Habsi. She has in mind a lot of 
				projects for the youth, retirees, people from social security 
				families, fishermen, disabled people and various other 
				marginalised people in the society.<br>
				<br>
				However, as Noora Al Zadjali who contests from the Wilayat of 
				Muscat made it clear, the candidates have a long way to go to 
				make people conscious about the significance of this elections 
				and what it hold in store for them in terms of their political 
				rights, economic and social development and stability. Speaking 
				at a media session orgainsed by Tawasul, Noora exhorted the 
				voters to study the list of candidates properly before they 
				exercise their electoral franchise. “We are in a transition 
				period. Therefore, select only those who can better serve you 
				and the country,” says Noora who is the director of information 
				department at the Ministry of Commerce and Industry.<br>
