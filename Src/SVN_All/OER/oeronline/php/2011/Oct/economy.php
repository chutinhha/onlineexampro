						<p align="justify" style="margin: 3px 10px">
						<span class="style27"><strong>Stronger role for women</strong></span><p class="style15">
				<strong><span class="style14">During the last 40 years, Omani 
				women have seen their political rights steadily increase and 
				their roles expand in government, business and education<br>
				<br>
				<font color="#808080"><i>By Oliver Cornock</i></font><br>
				</span></strong>
						<br>The best clue to a nation’s growth and development 
				potential is the status and role of women,” David S. Landes, 
				economics Professor Emeritus at Harvard, wrote in his book, The 
				Wealth and Poverty of Nations. Credible research on the topic 
				points in one direction: as women in a country become 
				better-educated and better-represented in the economy, that 
				country reaps major economic and social benefits.<br>
				<br>
				In fact, educating women is one of the highest-returning 
				investments a nation can make, according to a World Bank study 
				conducted by then-chief economist Lawrence Summers. His research 
				found that more educated women can moderate birth rates, improve 
				national health and raise education standards.<br>
				<br>
				In business settings as well, women tend to thrive. To take one 
				example, a 2008 study jointly produced by Columbia University 
				and the University of Maryland looked into the affect of women 
				decision-makers in business. After studying 1,500 top US 
				companies, the project concluded that companies with women in 
				their upper-management showed stronger performance, especially 
				in fields that required creativity and collaboration.<br>
				<br>
				The positive effects of a stronger role for women in society and 
				business have not been felt equally around the world, however. 
				For a number of reasons, women’s rights in the Middle East have 
				come to the fore as an issue more recently. As a result, women 
				represent a largely untapped source of economic and social value 
				in the region. A World Bank study on Middle Eastern labour 
				markets, for example, indicated that increasing the number of 
				women in the workforce could boost average household incomes by 
				up to 25 per cent without causing additional unemployment.<br>
				<br>
				Oman has been developing women’s rights at a quick pace relative 
				to its neighbours. His Majesty Sultan Qaboos bin Said al Said’s 
				coming to the throne in 1970 offered women a serious boost, and 
				ongoing reforms have been encouraging women to participate in 
				government, start their own businesses and seek better 
				educations.<br>
				<br>
				Although advances have been incremental, Omani women have seen 
				their political rights steadily increase. In 1994, His Majesty 
				made Oman the first country in the region to offer limited 
				women’s suffrage, upgrading those rights to provide universal 
				suffrage in 2003. In the same year’s elections 16 of the 584 
				candidates for the Council of State were women, two of whom were 
				elected. In addition, the Sultan currently has a number of women 
				in his cabinet, including Minister of Higher Education HE 
				Rawiyah al Busaidiyah and Minister of Education Madiha al 
				Shaibaniyah.<br>
				<br>
				Women’s progress in the civil service is also growing steady. 
				Between 1995 and 2008, the number of women in government jobs 
				has nearly quadrupled, increasing from 12,193 to 47,390, 
				according to the Ministry of National Economy’s Statistical 
				Yearbook. That ratio comes out to be about a third of government 
				workers.<br>
				<br>
				<b><font size="2" color="#000080">Woman entrepreneurs</font></b><br>
				<br>
				Women have also increased their presence in private firms. Of 
				the 158,315 workers registered with the Public Authority for 
				Social Insurance, 29,273, or 18.5 per cent, are women, according 
				to the Ministry of National Economy. The growth in small- and 
				medium-sized enterprises (SMEs) should help boost private sector 
				participation. In 2001, the Sultanate created an organisation 
				called SANAD to provide loans and other support to start-up 
				businesses in Oman. The organisation offers loans of up to 
				RO20,000 ($52,000) with government-subsidised interest rates of 
				2 per cent. Additional funding is available to collective 
				projects (those involving more than one person) and 
				high-priority sectors with high start-up costs (such as tech and 
				IT companies).<br>
				<br>
				Women have made impressive gains in taking advantage of SME 
				assistance, making up 50 per cent of recipients, SANAD told OBG. 
				A high ratio of female small-business owners could do much to 
				boost female employment, because SMEs are a powerhouse of 
				job-creation. In OECD countries, SMEs provide two-thirds of 
				employment, while in developing economies they represent up to 
				97 per cent, according to OECD secretary-general Angel Gurria. 
				As drivers of jobs growth, SMEs have the potential to not only 
				boost the Sultanate’s ratio of female employees but also its 
				ratio of female employers.<br>
				<font size="2" color="#000080"><b><br>
				Empowered by </b></font><b><font size="2" color="#000080">
				education</font></b><br>
				<br>
				Many of these gains have been made possible by the government’s 
				commitment to provide education for women. Before His Majesty 
				came to the throne in 1970, there was virtually no public 
				education offered to women. Now, however, the system is split 
				roughly down the middle up through grade 12. Female students 
				represented 49 per cent of total enrolment during 2009-2010 
				school year, according to official statistics.<br>
				<br>
				The situation is similar in higher education. Women made up 48 
				per cent of students at Sultan Qaboos University last year. 
				Under the leadership of His Majesty, Omani women have seen their 
				roles expand in government, business and education. The ruler 
				has repeatedly made clear that he places great value on 
				increasing women’s rights, an indication that further progress 
				can be expected. “We have given our full attention, since the 
				beginning of this era, to the participation of Omani women in 
				the march of the blessed Renaissance, so we have provided them 
				with education, training and employment opportunities and 
				supported their role and status in society,” he said in a 2009 
				speech to the Council of Oman. “We are convinced that the 
				country, in its blessed march, needs both men and women [like a] 
				bird in relying on both of its wings to fly high in the horizons 
				of the sky.” <br>
