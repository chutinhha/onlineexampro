				<font face="Verdana" size="1" class="style11" style="font-size: 11px">
				<b>The Porsche Panamera combines the features of a luxurious 
				grand touring sedan with an engaging, high-performance car. An 
				OER Review</b></font><p align="justify" style="margin: 10px; ">
				<font face="Verdana" size="1" class="style11" style="font-size: 11px">
				<br>
				The Panamera has yet again strayed well outside Porsche’s 
				well-trodden sports car heritage to become one of the world’s 
				best sports sedans. The 2011 Porsche Panamera is no four-door 
				coupe but a true ‘gran turismo,’ with tremendous power, great 
				handling, and ample room for four real-old adults. Our adoration 
				of the Panamera’s begins with its broad appeal, because this 
				four-door Porsche truly can be everything to everybody. First, 
				the Panamera can accommodate four full-sized adults with 
				exceptional comfort, as its interior space, materials and design 
				give passengers the feeling that they’re travelling in a private 
				jet rather than driving on the expressway.<br>
				<br>
				Second, the Panamera delivers astounding levels of performance 
				for driving enthusiasts. Acceleration from the V8-powered 
				Panamera is impressive, while the performance of the Turbo model 
				is rocking. The car’s handling limits are also surprising, as it 
				corners like a much smaller sports car. Even the ride quality 
				doesn’t suffer, as this athletic sedan can insulate its 
				occupants from the travails of the outside world just like a 
				fine luxury car. The front and rear fender styling are sleek. 
				The interior, on the other hand, is a stylish upgrade compared 
				to the dull, drab Porsche interiors of the past. And while the 
				Panamera feels intimate and sports-car-like in front, the back 
				seat is positively limo-like, with excellent space to sprawl 
				out, as well as good ride comfort.<br>
				<br>
				</font>
				<font face="Verdana" size="1" class="style11">
				<font color="#000080" size="2"><b>The powertrain </b></font>
				</font>
				<font face="Verdana" size="1" class="style11" style="font-size: 11px">
				<br>
				<br>
				The 2011 Panamera models for enthusiasts and true Porschephiles 
				and track hounds remain the Panamera S, with its 400-horsepower, 
				direct-injection 4.8-litre V-8 engine, making a peak 369 
				pound-feet, and the Panamera Turbo, which makes a stout 500 
				horsepower and 516 lb-ft. There’s scalding performance at hand; 
				the base engine in the Panamera S and 4S vaults the sedan to 60 
				mph in either 5.2 or 4.8 seconds. The Turbo charges to 60 mph in 
				4.0 seconds or less.<br>
				<br>
				Power in this front-engine car is sent to either the rear wheels 
				or all four wheels through a seven-speed, dual clutch PDK 
				automatic transmission that uses Porsche’s push-pull shift 
				levers. Equip the Sport Chrono package and you’ll be able to 
				change gears in manual mode, running the car right up against 
				its 6,700-rpm redline. Open the Panamera up on a road course at 
				140 mph and you’ll find the 911’s flat-six ripple replaced with 
				a more industrial-sounding whir that’s still distinctly Porsche.<br>
				<br>
				In addition to the seating room in back, the Panamera has the 
				benefits of a hatchback, as the backseats also flip down to 
				expose 44.6 cubic feet of cargo space, enough for two bicycles 
				with front wheels still attached. If you’re the passenger in 
				front, looking back, the Panamera can also feel like two cars in 
				one. From the front seats forward it doesn’t take much 
				imagination to think that you’re in an exceptionally plush 
				Porsche 911. Though the engine sound obviously isn’t quite the 
				same, the view out ahead is remarkably similar.<br>
				<br>
				The Panamera can also be had with nearly every option ever 
				imagined for a production luxury sedan, cruise control; 
				dual-zone climate control; leather seating; a panoramic sunroof; 
				a navigation system also used in the Cayenne that has crisp 
				displays and customisable maps; and Bluetooth control for 
				hands-free phone operation. As always, Porsche has again set new 
				trends and benchmarks <br>
				with the Panamera.<br>
