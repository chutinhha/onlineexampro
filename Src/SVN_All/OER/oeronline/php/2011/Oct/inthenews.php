						<p align="justify" style="margin: 3px 10px">
						<span class="style27"><strong>On the right track<br>
&nbsp;</strong></span><p class="style15">
				<span class="style14"><strong>The four-day Oman Transport 
				Infrastructure Summit deliberated on the opportunities and 
				challenges in the Sultanate’s transport sectoror<i><br>
&nbsp;</i></strong></span><p class="style15" style="text-align: center">
				<font face="Verdana" size="1">
				<img border="0" src="images/inthenews1.jpg" style="border: 1px solid #808080"></font><p align="justify" class="style13">
				Over 30 high-profile speakers and 120 local and international 
				industry professionals attended the Oman Transport 
				Infrastructure Summit, the first-ever event dedicated to the 
				roads, airports, ports and railways in the Sultanate as the 
				government embarks on massive transport and infrastructure 
				development throughout the country.<br>
				<br>
				The four-day event was held recently in co-operation with the 
				Ministry of Transport and Communications and was officially 
				inaugurated by Said bin Hamdoon Al Harthy, undersecretary for 
				ports and maritime affairs at the Ministry of Ports and 
				Communications. The formal opening ceremony was attended by 
				officials from ministries, diplomats, senior executives of 
				construction-related companies and industry experts in the 
				region’s transport and infrastructure sector.<br>
				<br>
				Says Al Harthi at his inaugural address, “The government is 
				looking to link all the three modes of transport – roads, sea 
				ports and airports. The interior roads will also be linked with 
				the main highways. Moreover, major ports will have connectivity 
				to domestic ports. He also revealed that the proposed shifting 
				of all commercial import and export activities from Muscat port 
				to Sohar, would be in a phased manner to make it smooth.<br>
				<br>
				Addressing the summit, Mohammed bin Aref Abu Zaki, road expert 
				at the Directorate General of Roads And Land Transport at the 
				ministry of transport and communications says strategic road 
				projects totaling more than 2,000 kilometres in length are 
				currently under various stages of design and development across 
				the Sultanate.<br>
				<br>
				“Expanding road network is not only designed to cater to traffic 
				growth, but also enhance connectivity with remote and isolated 
				communities, some of them located deep in wadis or mountainous 
				regions of the country. Oman’s road network has more than 
				quadrupled over the last 15 years, from 6,591 km in 1996 to 
				25,926 km by the end of 2009. The total length of graded roads 
				soared from 24,800 km in 1996 to 30,435 km in 2009. New projects 
				add on average 1,000 kilometres of new blacktop to the country’s 
				network every year,” Abu Zaki adds.<br>
				<br>
				According to Dr Syed Anisuddin, expert – training and 
				development at the Ministry of Defence engineering services, 
				there is a need to scientifically plan the future of Oman 
				transport system for providing a better level of service, an 
				environment-friendly sustainable light rail transport seems to 
				be the most viable solution for resolving the transport woes in 
				Muscat. He observes that the rapid urbanisation in the past few 
				years in Oman has resulted in abundant increase in housing and 
				vehicular population, which is one of the highest in the world. 
				“Every year, the road length is increased by 20 per cent. 
				However, the road space becomes scarce and the passengers are 
				witnessing difficulties,” he adds.<br>
				<br>
				Throughout the four days, the attendees discussed the main 
				challenges faced by Oman’s transport infrastructure sector, 
				exchanging best practices and strategies to overcome those 
				challenges and learning about the latest technologies and 
				materials available in the transport infrastructure industry.<br>
