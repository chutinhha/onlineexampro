						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">
						Taking a three-month call</font></b><p class="style15">
				<strong><span class="style14">Given the traumatic macroeconomic, 
				financial and political events of August and September, it is 
				better for investors to examine the outlook for equities, crude 
				oil, foreign currencies, gold, debt and emerging markets in the 
				next three months<br>
				<br>
				<font color="#808080"><i>By Matein Khalid</i></font><br>
				<br>
				<br>
				</span></strong>Crystal ball gazing in financial markets is a 
				dangerous business unless the gazer is forced to periodically 
				swallow crushed glass when his forecasts go wrong. 
				Unfortunately, investors are forced to discount the future in 
				real time to have any hope of making money in the markets. The 
				macroeconomic, financial and political events of August and 
				September were so traumatic that it makes sense to project the 
				outlook for equities, crude oil, foreign currencies, gold, debt 
				and emerging markets in the next three months. The idea is to 
				analyse the risk-reward calculus in each market and the 
				relationships among them.<br>
				<br>
				The US stock market rally that began in July 2010, goosed by the 
				Bernanke Fed’s $600bn bond purchases (QE2), ended with a 
				vengeance in August as Uncle Sam’s credit rating was downgraded 
				by S&amp;P while economic data (jobs, exports, industrial 
				production, home prices) slowed down. The recent fall from the 
				S&amp;P 500 peak was 18 per cent, significant technical damage on 
				the charts. American equities are now priced for a mild 
				recession in 2012 and $100 index earnings are unrealistic. Where 
				will the S&amp;P 500 index trade in the next three months? I believe 
				earnings will be revised lower, the macro data will continue to 
				be mediocre at best, we could well trade below the August lows 
				at 1,120 on the S&amp;P 500 index. The market will than struggle to 
				find a bottom in the 1,040-1,060 range.<br>
				<br>
				Recession risk in the US and Europe, a potential slowdown in 
				China and the politics of the ECB, EU/IMF Greek bailout will 
				keep investor uncertainty high. This will prevent a bullish 
				rerating of valuation multiples. Net-net, I believe there is 
				still 10-12 per cent downside risk in equities. As the dollar 
				rises, the S&amp;P 500 breaks below its August lows, offshore funds 
				redeem, emerging markets will continue their downtrend. Yet a 
				bottom in China, Brazil, India, Russia and South Korea could 
				emerge this winter. Asia faces stagflation, as growth slows 
				while inflation runs at three year highs. This is a negative 
				milieu for emerging markets.<br>
				<br>
				<b><font size="2" color="#990000">Reasons for Brent’s fall
				</font></b><br>
				<br>
				Brent crude could fall by $20 in the next three months. Why? Six 
				reasons: One, the US economy has clearly entered a soft patch, 
				though not yet double dip recession. GDP growth has been revised 
				lower. The unemployment rate is stubbornly high at 9.1 per cent 
				and the world’s largest economy, a $15tn monster, created no new 
				jobs in August. Two, the Eurozone sovereign debt crisis has now 
				escalated into contagion risk in the bank funding markets. Three 
				month LIBOR is rising. Deutsche Bank shares lost 30 per cent of 
				their value in a month. Italian and Spanish bond markets are on 
				edge while Greece has priced in a default. Germany is no longer 
				the economic locomotive of Europe.<br>
				<br>
				Three, a higher dollar and higher volatility (Chicago VIX index 
				above 40) forces hedge funds to slash leveraged exposures to 
				risk assets, including oil. After all, NYMEX trades one billion 
				barrels in paper oil (futures contracts), twelve times the 
				global physical oil demand. Four, Libya will return to the oil 
				export market in a year, according to the IEA. This means the 
				geopolitical risk/oil supply premium implicit in current prices 
				will unwind. Five, OPEC production at 30 million barrels a day 
				is the highest since 2007.<br>
				<br>
				Saudi Arabia basically abandoned the post-Lehman OPEC quota 
				system at the last Vienna meeting. The Saudis have not cut 
				output now that the Gaddafi regime has fallen. Six, the IEA 
				demand forecast of 89.5 million barrels a day in 2012 is unreal. 
				As Europe, US, China and India GDP growth decelerates, so will 
				gasoline and distillates demand. The IEA will be forced to cut 
				its demand forecasts. Brent can well fall to $80-85 by December.<br>
				<br>
				The foreign exchange markets will see major changes in the next 
				three months. The Swiss National Bank decision to peg the Euro 
				at 1.20 means the Swiss franc is no longer a credible safe haven 
				for global investors spooked by the Eurozone debt crisis. Even 
				at 0.85-0.86, the Swiss franc is “absurdly” overvalued against 
				the dollar, according to the SNB. The peg means Swiss export 
				share can significantly rise, as do the SNB foreign currency 
				reserves. The dollar will finally begin to rise against the 
				Swiss franc. I can envisage the Swiss franc at 0.92 to 0.94 in 
				the next three months. It is also inevitable that the ECB can no 
				longer tighten monetary policy as Europe’s economic growth 
				decelerates sharply, led by Germany. A ECB rate cut in October 
				or November is now a highly probably scenario. Jean-Claude 
				Trichet will be forced to reduce his inflation risk forecasts 
				even as he is succeeded by Mario Draghi as the new ECB 
				President. The ECB policy rate can well fall to 1 per cent by 
				early 2012. This means the Euro-dollar can well fall to 
				1.30-1.32, particularly if the politics of the Greek bailout 
				unravels or Chancellor Angela Merkel’s coalition is further 
				undermined in German politics.<br>
				<br>
				As global growth slows, the current bubble in commodities will 
				unwind with a vengeance, particularly if Chinese GDP growth 
				falls below 8 per cent as export orders fall and the PBOC 
				monetary tightening bites. This means the Canadian, Australian 
				and New Zealand dollar are extremely overvalued and can well 
				fall by 5-8 per cent in the next three months. The Aussie 
				dollar, for instance, can well test parity against the US 
				dollar, well below its current 1.06 range.<br>
				<br>
				As global growth slows, the dollar rises, gold prices could move 
				lower to the $1600-1650 range even though a ultra-accommodative 
				Fed, zero real interest rates and the tail risk of a disruptive 
				Eurozone sovereign default will ensure that the secular uptrend 
				in the yellow metal will remain in place. Copper and silver, 
				however, face significant downside risk as industrial production 
				worldwide decelerates. <br>
				<br>
				This is a disaster for base metals.<br>
				<br>
