						<font face="Verdana" size="4" color="#C00000">
						An enabling environment </font></b><p align="justify" class="style13">
				<br>
				Individual capacity is vital for a person entering the corner 
				office. But the fact remains that currently the overwhelming 
				majority of people occupying these offices are male. OER’s 
				annual, ‘The Most Powerful Women in Business (MPW)’ issue 
				celebrates women luminaries who have made it to the top despite 
				daunting odds like cultural barriers and deep rooted prejudices.<br>
				<br>
				Employing more women is not only in the interests of women but 
				also of companies. Research has shown that employee diversity 
				improves corporate performance. For example, The Fortune 500 
				companies which are in the top quartile in employing women 
				outperform those in the lowest quartile by a large percentage.<br>
				<br>
				Understanding women as consumers is also crucial for companies, 
				and who better to do so than a company’s female employees? 
				Getting more women into the workplace who understand the buying 
				preferences of women consumers is important. Thus companies that 
				hire and retain more women are not only doing the politically 
				correct thing but also giving themselves a competitive edge. On 
				an average women employees are more loyal and stay back in 
				organisations for longer periods of time than men.<br>
				<br>
				So should organisations make an extra effort to retain their 
				female employees? Not many companies allow flexi-hours, feeding 
				hours and similar enablers to help women employees through their 
				overworked periods. Probably not, if women want to rise to the 
				top they should not bank on chivalry, kid gloves or affirmative 
				action to help their cause. The touchstone of excellence should 
				be pure meritocracy and nothing else. Our MPW exhibit a killer 
				work ethic and determination.<br>
				<br>
				But the least that companies can do is to shed their prejudices. 
				There are also some means that women can use themselves to 
				prosper professionally, such as projecting themselves better. 
				Tapping into informal networks or finding themselves mentors 
				also works to women’s advantage. Finally, family support is 
				critical for women to build a career. Are men listening?<br>
				<br>
