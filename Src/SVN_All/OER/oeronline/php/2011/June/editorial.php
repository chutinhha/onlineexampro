						Living a green life</font></b><p align="justify" class="style13">
				<br>
				<b><font size="7">T</font></b>he 9.0 magnitude earthquake that 
				hit Japan on March 11 brought us face to face with the 
				destruction that nature’s fury can wreak. Coastal towns, cars 
				and buildings were swept away like toys by marauding waves. The 
				powerful quake shifted the earth on its axis and shortened the 
				length of a day by 1.8 millionth of a second. Once the water 
				receded, the economic toll of the tsunami became clear. The 
				Toyota Stock Exchange lost $700bn in three days. Sony suspended 
				production at seven plants; Toyota slipped from being the 
				largest manufacturer of cars worldwide and supply lines are 
				still uncertain. The nuclear leak at the Fukushima Daiichi 
				plant, put a question mark on nuclear energy as a source of 
				power. Switzerland announced a freeze on new nuclear plants, 
				Germany shut facilities built before 1980 and people in India 
				took to the streets to protest against nuclear plants. It’s been 
				close to three months since the tragedy and the havoc caused by 
				it is still fresh. The tsunami has brought the environment issue 
				centrestage yet again.<br>
				<br>
				Oman Green Awards 2010 was born out of a conviction that saving 
				the environment is no longer a matter of choice but a necessity. 
				The overwhelming response to the initiative from Oman’s 
				corporate sector reinforced our belief that we were on the right 
				path. Oman Green Awards 2011 takes forward the promise of a 
				green Oman. To reinforce our commitment on green living, we have 
				printed OER’s June issue entirely on recycled paper. Hopefully, 
				this should save a few trees from being felled.<br>
				<br>
				To raise the intellectual bar on environment issues, we decided 
				the look beyond the obvious issues. The result is OER’s cover 
				story which takes an in-depth look at two issues that merit 
				immediate attention in the Sultanate – wastewater management and 
				solid waste management. I am sure you are going to hear a lot 
				about these topics in the months to come. The other big story in 
				this issue, Young executives to watch out for, showcases the 
				success, dreams and aspirations of Omani youth. Let me repeat 
				myself – these are stars whom you are going to hear a lot about 
				in the coming years.<br>
