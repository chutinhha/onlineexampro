				profession in his blood – a legacy he inherited from his father 
				who retired from Gulf International Bank (GIB) in Bahrain. This 
				young and aspiring executive who aspires to be a CEO of a bank 
				in future grew up watching his father climb up the career 
				ladder. That is why he attributes his professional success to 
				his dad who served as a mentor and a role model.<br>
				<br>
				After having his schooling in Bahrain, Khalid moved to UK to do 
				his BA (Hons) in Economic and Business Policy from the 
				University of Portsmouth (presently known as Portsmouth Business 
				School). This was followed by a Masters in Economics from the 
				same university. <br>
