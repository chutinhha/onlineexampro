				job was a challenging responsibility as Ahli Bank was a new 
				player in the market and it was the beginning of the financial 
				crisis. But Khalid’s sound academic and professional background 
				helped him take it up with self-confidence. “I am tasked with 
				generating business from large corporates in Oman; and I look 
				after general trading and multinational companies. Though the 
				last two years has been challenging because of the financial 
				meltdown, our corporate banking business and the bank as a whole 
				has done very well,” says Khalid.<br>
				<br>
				As a leader, Khalid firmly believes in total team involvement. 
				“You need to get your team to buy into the vision or target 
				before you can achieve it. When someone has a good idea, 
				recognise it, reward it and let them take ownership of it. A 
				manager always takes credit while a leader gives the credit to 
				others,” he avers.<br>
