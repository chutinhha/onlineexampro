				<img border="0" src="images/HR2.jpg" width="130" height="161" style="border: 1px solid #808080; " align="left"></font>Oman 
						has to strike a balance between its actual requirements 
						and its natural beauty and traditional architecture, 
						says Ammar Suleiman Al Kharusi, Supervisor, Omran<br>
						<br>
						As a civil engineer with an astute and pioneering vision 
						for Oman’s urban development, Ammar Suleiman Al 
						Kharusi’s passion for property development begins from a 
						very tender age. He considers his current stint with 
						Omran as a call of destiny to work for materialising his 
						colourful dreams of a beautiful Oman studded with 
						attractive tourism destinations. <br>
						<br>
						Ammar who envisages a prospective future for the tourism 
						and construction sectors in the country believes that 
						each country or city has its own characteristics and 
						architectural identity. “We need to develop Muscat as 
						Muscat and not in the mould of any metropolis. For that 
						we have to harmonise the natural beauty of Oman and its 
						traditional architecture with the actual needs of our 
						time.” His vision for Oman’s urban development can be 
						summed up in his own words: “Develop the right thing 
						with right attitude and with right financials.”<br>
						<br>
						Hailing from an educated family, Ammar attributes his 
						success to his parents who were very particular about 
						the quality of their children’s education. Three of his 
						five sisters are doctors. He calls himself a proud 
						alumnus of National Private School, Al Khuwair. After 
						schooling, he left for UAE to do his Bachelor’s in Civil 
						and Environmental Engineering from the University of 
						Sharjah. Immediately after completing his studies, he 
						started working in UAE where he found immense 
						opportunities for a young civil engineer to thrive and 
						build up his career. With a lot of high-profile 
						projects, UAE offered him better exposure and a more 
						competitive environment to grow. “I got a chance to work 
						with Tenure Construction International, a reputable 
						project management company, and to be part of a number 
						of high-profile and prestigious projects such as Dubai 
						mall and various other projects in Abu Dhabi,” says 
						Ammar.<br>
						<br>
						The years spent in UAE as a student and a professional 
						helped Ammar make a solid foundation for his career. 
						However, he always cherished coming back to Oman to 
						apply his skills and experience and implement his vision 
						of urban development in the country. After five years, 
						he came back to Oman in 2010 to start his innings with 
						Omran. “I was lucky to join a company of Omran’s stature 
						which seldom recruits young professionals like me. 
						However, it has a comprehensive policy of Omanisation 
						and, in my case, my experience abroad in a more 
						competitive environment was <br>
						greatly helpful.”<br>
						<br>
						It is unsurprising that Steve Jobs, the founder and CEO 
						of Apple Inc., has cast a spell on him. The 
						perseverance, dedication and innovation with which Jobs 
						braved the challenges in his chequered career to emerge 
						as one of the successful business magnate and inventor 
						of our time has been inspiring for Ammar. He is also a 
						guitar hobbyist and a fan of James Hetfield and Jimmy 
						Page.  

