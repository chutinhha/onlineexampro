				four years, he was manager of the company’s Network Operation 
				Centre. Before joining Nawras, Monther has a short stint as the 
				territory account manager of Awaya, an international company. 
				Though initially he was working from home and was reporting to 
				the regional office in Dubai, he managed to set up an office in 
				Oman with a sales and support team comprising of 15 members.<br>
				<br>
