				a passion for her, you may not believe it, on the face of it. 
				But you cannot disagree with her as she goes on citing the 
				reasons for it and describing the underlying philosophy of a 
				process which starts from nothing and ends up preparing a 
				delicious stuff which makes your mouth water. It is this quest 
				for novelty and a sense of satisfaction in whatever she does 
				that makes her different and enabled her to be the only woman 
				among the three finalists of BizPro Awards 2011.<br>
				<br>
				Aliya does not believe in doing something for nothing or just to 
				serve a ceremonial purpose. That is why she was initially 
				reluctant to do an MBA which she thought was nothing more than 
				an ornamental addition to her résumé. But, once she realised the 
				importance of the course in adding value to her mission and 
				achieving her goals in life, she decided to do it. Aliya greatly 
				treasures the support and motivation she got from her parents 
				throughout her life. She is very much obliged to her mother who 
				sent her abroad for studies and stood by her at all junctures of 
				her educational and professional life. Aliya who did her entire 
				schooling in India earned her Bachelor’s in Electronics from MH 
				Saboo Siddik College of Engineering in Mumbai. She also earned a 
				diploma in electrical and video engineering. She believes that 
				her stay and study abroad have been instrumental in moulding her 
				personality and sharpening her skills thereby contributing to 
				her professional success.<br>
				<br>
				Coming back to Oman in early 2004, she joined Mott MacDonald as 
				control &amp; automation engineer. Though she has just completed 
				seven successful years in Mott MacDonald, Aliya knows very well 
				when consistency gives way to complacency. “I don’t like to be 
				complacent; I don’t like to do monotonous things. I am here 
				because I enjoy my work and gain a lot of knowledge. I am very 
				focused on what I am doing and do not want to be a jack of all 
				trades and a master of none,” says a determined Aliya. She 
				always sought to go beyond her comfort zone taking up new 
				responsibilities and braving challenges. <br>
