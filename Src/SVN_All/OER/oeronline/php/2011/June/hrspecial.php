				cherished coming back to Oman to serve his homeland. Besides, as 
				a growing economy Oman has offered a lot of opportunities for 
				young Omanis like him in various sectors including banking. 
				After his higher studies in UK, Khalid came back to Oman and 
				joined National Bank of Oman (NBO) as a management trainee. 
				Later, he joined HSBC as an executive management trainee. It was 
				during this time that he was sent to UK for a two-month 
				executive trainee development programme. Soon after completing 
				the course, Khalid was promoted as a Manager in HSBC. Before 
				leaving HSBC in 2008, he was again promoted as a senior 
				relationship manager.<br>
