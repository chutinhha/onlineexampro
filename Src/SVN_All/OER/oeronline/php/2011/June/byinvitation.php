						<span class="style27"><strong>A tenuous correlation</strong></span><p align="justify" style="margin: 3px 10px">
						&nbsp;<p align="center" style="margin: 3px 10px">
				<font face="Verdana" size="1">
				<img border="0" src="images/byinvittion1.jpg" style="border: 1px solid #808080; " class="style28" width="450" height="284"></font><p class="style15">
				<span class="style14"><strong>Can oil’s clout continue to 
				protect Oman’s stock market from regional and local unrest?<font color="#C0C0C0"><br>
				</font><font color="#808080"><i><br>
				By Ahmad Ayyub and Faisal Sultan</i></font><br>
&nbsp;</strong></span><p align="justify" class="style13">
				The recent geopolitical developments in the region and in Oman 
				have had a telling effect on the Muscat Securities Market (MSM). 
				Between January and March the events in Tunisia, Egypt and Oman 
				have resulted in major fluctuations in the stock market although 
				unrest within Oman’s borders has had a greater and longer 
				lasting effect.<br>
				<br>
				The events in Tunisia which started in December 2010 and 
				culminated in the ouster of the Tunisian President on January 
				14, 2011 resulted in a 33-point drop (0.47 per cent) in the MSM 
				30 Index. The market was quick to recover going back to 
				pre-unrest levels in just two trading days.<br>
				<br>
				The events in Egypt which started on January 25 and resulted in 
				the ouster of the Egyptian President on February 11 caused the 
				Index to drop by 252 points (3.6 per cent). This time, the 
				market was slow to recover as it took 14 trading days for the 
				MSM 30 index to go back to pre-unrest levels and recover the 
				losses.<br>
				<br>
				The steeper decline and longer recovery time on this occasion 
				was due to a number of reasons. First, Egypt is a much bigger 
				trading partner with Oman than Tunisia. In 2009, non-oil trade 
				with Egypt totalled RO96mn as against RO3mn with Tunisia. 
				Second, developments in Egypt got greater media coverage than 
				those of Tunisia and by then similar unrests were beginning to 
				sprout in other Arab countries causing cautious fear in regional 
				markets. Third, Egypt is geographically closer to Oman than 
				Tunisia. And fourth, it took a longer time for the Egyptian 
				President to step down (MSM recovered completely two trading 
				days after Hosni Mubarak stepped down).<br>
				<br>
				The events in Oman which started peacefully in January but 
				turned belligerent in February first affected MSM on February 
				15. The MSM 30 index continued to drop until February 28 losing 
				859 points (12.3 per cent) in this period. The sharpest daily 
				declines in the MSM 30 occurred on February 27 and 28 (decline 
				of 187 points and 316 points respectively), the days when 
				tensions escalated. The market saw negligible recovery in the 
				days following these events, reflecting a temporary loss of 
				confidence by investors.<br>
				<br>
				Events outside the borders of Oman did not have a significant 
				long-term impact on the stock market. Investor fears that events 
				in Tunisia and Egypt would have an economic impact on Oman were 
				short-lived. However, when political unrest reached Oman and 
				turned unexpectedly violent, the market dropped sharply with 
				fears that the unrest would continue and that there would be a 
				negative effect on the economy. The market has yet to recover to 
				pre-protest levels.<br>
				<br>
				Upon further examining the movement of funds in MSM, it is 
				apparent that the unrest in Oman triggered an outflow of 
				international investment in the stock market. MSM registered a 
				net weekly outflow of international investments in stocks 
				between the week ending on January 13 and the week ending on 
				March 31. The largest outflows occurred in the month of February 
				when protests spread to a number of towns in the country and it 
				became evident that the protests would not be simple one-off 
				events. Net outflows of funds by international investors reached 
				RO23mn in that month.<br>
				<br>
				<b>Oil price impact</b><br>
				<br>
				The effect of the unrest in Oman has had some far reaching 
				implications. As an oil-based economy, the performance of the 
				stock market in Oman has been closely correlated to the price of 
				oil. Between January 2007 and December 2010, the MSM 30 index 
				and the OPEC basket price showed a 91 per cent correlation. 
				Whenever the price of oil increased or decreased, the MSM 30 
				Index moved accordingly.<br>
				<br>
				However, since the beginning of the unrest in the country, the 
				MSM 30 Index has become negatively correlated to the OPEC basket 
				price. The price of oil kept edging upwards while the MSM 30 
				index plummeted. This shows that no matter how much more revenue 
				the government makes from oil exports, there cannot be economic 
				stability without social stability.<br>
				<br>
				Prior to the latest developments in Oman, the country was able 
				to rely on oil revenues to protect its stock market and economy 
				from the ramifications of external economic and political 
				events.<br>
				<br>
				The latest developments within the borders of Oman have changed 
				this, at least in the short-to-medium term. The effect of social 
				instability in Oman simply outweighs the benefits of higher 
				government revenues from oil. It would be interesting to see if 
				the MSM 30 index manages to regain its correlation to the price 
				of oil in the near future.<br>
