						<span class="style27"><strong>For a better tomorrow</strong></span><p class="style15">
				<strong><span class="style14">Oman Green Awards will honour 
				companies and individuals furthering the green cause in Oman on 
				June 5, 2011</span></strong><p class="style15">
				&nbsp;<p align="justify" class="style13">
				<font face="Verdana" size="1">
				<img border="0" src="images/environment1.jpg" width="150" height="153" style="border: 1px solid #808080; " align="left"></font><b><font color="#009933">Oman 
				Green Awards (OGA)</font></b><font color="#009933"><b>,2011</b></font> 
				has rolled out the ‘green’ carpet for its prestigious awards 
				ceremony to be held on June 5 at Al Bustan Palace Hotel, Muscat. 
				HE Mohammed Said Al Toobi, Minister of Environment and Climate 
				Affairs, will preside over the function as the chief guest.<br>
				<br>
				Finalists from nine different categories, as well as a host of 
				environment enthusiasts and conservationists, will be a part of 
				this novel environment venture that has, at its heart, the basic 
				values of environment conservation and development of a green 
				lifestyle among the general public. OGA is being held in 
				association with the Ministry of Environment and Climate 
				Affairs, Muscat Municipality, Ministry of Health and Environment 
				Society of Oman. Representatives from these august organisations 
				will attend the green celebrations, which coincide with the 
				World Environment Day.<br>
				<br>
				A worthy cause <br>
				<br>
				The event, an Oman Economic Review initiative from United Media 
				Services, has received massive support from corporate houses and 
				individuals who have made green mission a priority. Vale, Haya 
				Water, MB Holding Company, Panasonic and Al Bustan Palace 
				InterContinental Muscat have joined OGA as Green Oman partners 
				to keep the green torch burning bright in the country. The media 
				partners of the event include Times of Oman and Al Shabiba. 
				Support partners of the event include Business International 
				Group, Oman Printers and Stationers, Green Cover and Infoline.<br>
				<br>
				OGA has got an overwhelming response from Oman’s corporate 
				sector. In 2010 and 2011 the Award has received over 200 entries 
				raising environment consciousness in the Sultanate. The platform 
				has encouraged a number of companies to start or enhance 
				conservation efforts.<br>
				<br>
				An august panel of judge will adjudicate and the winners will be 
				announced and awarded on June 5. Effectiveness, innovation and 
				creativity, impact, originality and leadership and continuity 
				and sustainability of the initiatives are the core values that 
				will guide the judges in identifying business houses, 
				institutions and individuals that are involved in promoting and 
				implementing environmental programmes.<br>
				<br>
				Awards will be presented in the following categories – The Green 
				Innovation Award; Green Campaign of the Year; The Green Habitat 
				Award; The Green Champion; Green Research Award; Green Landscape 
				Award; Green Footprint Award; Green Education Award, and Green 
				Guardian Award. In addition a Special Jury Award and Public 
				Choice Award will be presented as special awards.<br>
				<br>
				OGA, which was conceptualised and implemented a year ago to 
				awaken eco-consciousness in every Omani citizen as well as 
				resident, is focussed on motivating behavioural change as well 
				as increasing awareness in relation to protection and 
				preservation of environment. It is a celebration of their vision 
				for a cleaner and greener tomorrow. Its three-fold objectives 
				focus on boosting local environmental efforts and achievements; 
				providing green groups a platform for concerted action; and 
				raising awareness and generating greater action. More 
				information on OGA can be got from<i><font color="#008000">
				</font></i><a href="http://www.omangreenawards.com">
				<font color="#008000">www.omangreenawards.com</font></a> .<br>
