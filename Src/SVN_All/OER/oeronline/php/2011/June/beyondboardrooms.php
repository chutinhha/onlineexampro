				Muthanna Al-Durrah, CEO, Genetco has the proverbial Midas touch 
				when it comes to establishing new businesses. Having completed 
				his PhD from Purdue University, US in 1982, he joined the team 
				that started IKEA in Kuwait, in 1984. Over eight years he worked 
				with several project developments like Arby’s fast food, Bertie, 
				a shoe agency from UK, NAPCO and many others.<br>
				<br>
				“I worked as a portfolio manager for Al Homaizi Group, one of 
				the largest business groups in Kuwait, managing real estate 
				development, funds, and investments. During Iraq’s invasion of 
				Kuwait, I maintained operations of the fund out of London, and 
				then I moved to Cairo with the same group,” says Dr Muthanna.
				<br>
				<br>
				After working in real estate development in the early 1990s, he 
				went onto establish the Arby’s chain in Egypt in 1994. He opened 
				five outlets in quick succession, one of which soon became the 
				seventh highest selling Arby’s in the world with over $2mn 
				annual revenue. Arby’s has over three thousand outlets in the 
				world. <br>
				<br>
				In 1998 he took on the responsibility of developing RadioShack 
				in the Middle East. Over the next 10 years, RadioShack, Middle 
				East grew from scratch to become a $100mn company with 96 stores 
				in the region. “We became RadioShack’s third largest 
				international business after Mexico and received numerous 
				awards,” he avers. In 2006 he established another company called 
				Mobile Shop, setting new benchmarks for display and branding in 
				the region. The result -- 74 outlets in Egypt in a span of two 
				years. In 2009, he moved to Oman with Genetco, a leading company 
				in the Sultanate. Genetco has two main lines of business – 
				Engineering and Trading in consumer durables. The company has 
				several large MEP and facility management contracts, many 
				renowned brands, over 150 dealers and 1,600 employees.<br>
				<br>
				<b><font size="2" color="#006600">Success principles</font></b><br>
				<br>
				“You stand a better chance of success establishing a 
				well-recognised brand or franchise as you benefit from its 
				recognition and experience; moreover, the principal helps in 
				setting initial systems and policies with an already tried 
				product or service. One factor which is immensely strategic is 
				human resources. Whenever I establish a new business or 
				franchise, I select young graduates and train them and help them 
				grow with the business,” adds Dr Muthanna. <br>
				<br>
				Elucidating his management style he states that he is a strong 
				believer in interacting with every aspect of the business 
				without losing the strategic planning of the organisation. He 
				does not like sitting in an ivory tower. “I am always involved 
				in the details of the business for two reasons – one, to have a 
				feel of every aspect of the business and thereby work as a 
				maestro to ensure that all divisions are working in harmony. 
				Success is achieved with this strategy along with the focus on 
				growing sales and this is something that I think about all the 
				time. ”<br>
				<br>
				His advice to the young generation is simple and profound: 
				“Focus on a career that you choose for yourself and do not move 
				around too much in your career making frequent shifts; it will 
				give you a thin penetration and understanding of the business. 
				Work hard, look into the details, and be loyal to the company 
				that you are working for.”<br>
