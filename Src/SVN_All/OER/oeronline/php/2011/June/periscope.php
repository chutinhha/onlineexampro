						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">
						Investing in bank shares</font></b><p class="style15">
				<strong><span class="style14">Investors can well accumulate 
				shares of global banks at modest valuations<br>
&nbsp;</span></strong><p align="justify" class="style13">
				Bank shares have not exactly been winners on Wall Street in 
				2011. The Volcker Rule limits on proprietary trading, Dodd Frank 
				restrictions on derivatives, excessive liquidity on balance 
				sheets, weak US loan demand, low margins, Uncle Sam’s aversion 
				to dividend hikes and share buybacks, investor skepticism about 
				the strategic future of Citigroup and Bank of America have all 
				slammed the shares of money centre banks. Bank shares have been 
				the worst performing sector of the S&amp;P 500 in 2011 and highly 
				correlated to bearishness about GDP growth, even though the US 
				Treasury yield curve has been extremely weak. However, amid the 
				doom and gloom, investors can well accumulate shares of global 
				banks at modest valuations.<br>
				<br>
				Take Citigroup (C), whose shares are 4.50, down from 5.50 last 
				summer (pre-reverse stock split prices). The bank earned $10.8bn 
				in 2010 after a $1.6bn loss in 2009, though trading and North 
				American consumer bank revenues are still mediocre. Citi trades 
				at eight times forward estimates of 0.56 and can well slash its 
				$26bn loan loss provisions down to the 13-15bn dollar range. If 
				so, Citi shares offer 30 per cent upside to six dollars.<br>
				<br>
				Goldman Sachs shares have fallen below 150 even as capital 
				markets (M&amp;A advisory IPO, debt and equity underwriting, 
				Euromarkets) revenues have surged in 1Q 2011. Goldman has 
				repurchased Berkshire Hathaway’s 10 per cent preferred dividend 
				shares, a boost for future earnings power. The golden age of 
				prop trading is over but Goldman can well earn $15 in 2011 and 
				$17-18 in 2012. The Abacus CDO haunts Goldman Sachs in the SEC, 
				Senate, media and the court of public opinion. I believe Wall 
				Street’s top investment bank is a buy at 145 for a 180 target.<br>
				<br>
				Apart from a mini-rally in February and March, I expect Indian 
				bank shares to correct by 20 per cent in the next three months 
				as the RBI is behind the inflation curve and at least three more 
				rate hikes are inevitable. Loan growth, net interest rate 
				margins, non performing loans and EPS metrics will all contain 
				treacherous landmines in the next three months even as rising 
				risk aversion will hit emerging markets as an asset class and 
				dampen capital inflows into Dalal Street. The slump in 
				industrial production is bearish for corporate profits, retail 
				sales and, consequently, bank loan spreads/NPL. The macro risk 
				in Indian banking exceeds the current valuation metrics of 
				Indian bank shares, particularly for public sector banks exposed 
				to greater earnings risk.<br>
				<br>
				<font color="#008000" size="2"><b>Kiss of death</b></font><br>
				<br>
				In any case, rising inflation and higher interest rates are the 
				kiss of death for banking NPLs, particularly since the RBI has 
				withdrawn 70 per cent blanket provision coverage requirements. 
				It would not surprise me to see Sensex at 17,000 by October. 
				ICICI’s New York ADR is expensive at 48 and I expect India’s 
				leading private bank to trade down to 40-42. This would equate 
				to a 150-200 rupee hit on the shares in Mumbai. A strategy to 
				generate 50 per cent returns if ICICI falls would be to buy the 
				December ICICI put options (48 strike) at 46. If I am correct, 
				this strategy offers a 50 per cent return on risk capital if the 
				bank’s New York ADR falls to 42. Somewhere near 800 rupees, 
				ICICI would once again be a compelling buy.<br>
				<br>
				Egyptian banks trade at some of the lowest valuation metrics in 
				MENA and the banking system is generally liquid with low 
				exposure to foreign currency debt. The stock market has endured 
				trauma, capital flight and a 40 day closure after the political 
				crisis that began in Tahrir Square in January and culminated in 
				the resignation of President Mubarak and the arrest warrant for 
				former finance minister Youssef Boutros Ghali. I would avoid 
				Egyptian banks even though the market multiple is only 10 times 
				earnings since the risk of currency depreciation is now 
				extremely high. I can easily envisage the Egyptian pound at 6.50 
				against the dollar by year end 2011 from the current 5.94 
				because GDP will contract as tourism, remittances and capital 
				flows plummet, even though Sinai LNG sales, cotton, Suez Canal 
				shipping toll revenues are still intact.<br>
				<br>
				Egypt’s central bank hard currency reserves are now down to only 
				$30bn and it would need an IMF loan in the event of a 
				speculative assault on the pound or if foreign investors fail to 
				rollover government issued Treasury bills. Bank shares would be 
				gutted if new political and economic black swans unsettle the 
				stock market. I would wait until the elections to re-enter 
				Egyptian bank shares.<br>
				<br>
				QNB, Qatar’s flagship commercial bank, will complete a jumbo 
				QR12.7bn rights issue that will increase its tier one Basel 
				capital ratio to an impressive 23 per cent. This means loan 
				growth metrics can well rise by 20-24 per cent in the Middle 
				East’s fastest growing economy. The rights issue will boost 
				QNB’s EPS and return on equity (ROE) at a time when its 
				economies of scale and demonstrated government support can well 
				enable it to increase its marketshare. QNB is not expensive at 
				2.3 times book value and 12 times forward earnings. QNB offers 
				20 per cent upside if its valuation metrics are re-rated in 
				Doha.<br>
