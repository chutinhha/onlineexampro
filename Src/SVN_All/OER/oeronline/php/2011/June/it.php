						<span class="style27"><strong>
						<a name="TYERS">Shared Vision</a></strong></span><p class="style15">
				<font face="Verdana" size="1"><strong><span class="style14">
				The Gartner Enterprise Architecture Summit 2011 demonstrated the 
				value of Enterprise Architecture and showed how to broaden its 
				influence both inside and outside the enterprise. Mayank Singh 
				reports exclusively from London<br>
&nbsp;</span></strong></font><p class="style15" style="text-align: center">
				<img border="0" src="images/IT.jpg" width="500" height="124"><p class="style15">
				<b>
				<br>
				</b>The Gartner Enterprise Architecture Summit, held on May 
				9-10, 2011, in London discussed key issues facing the Enterprise 
				Architecture (EA) Industry. EA is a key strategic initiative, as 
				the majority of practitioners have shifted their focus to their 
				business’s strategic vision, not focusing on the IT organisation 
				alone, according to Gartner, Inc. By 2016, 30 per cent of EA 
				efforts will be supported as a collaboration between business 
				and IT, up from nine per cent in early 2011.<br>
				<br>
				“The discipline of EA has passed a tipping point where 
				practitioners are moving beyond applying EA to IT alone, either 
				in aspiration or in actions,” says Betsy Burton, vice president 
				and distinguished analyst at Gartner. <br>
				<br>
				As with many traditional IT practices, the discipline of EA has 
				been gradually evolving for several years to become more closely 
				integrated and aligned with business strategy. However, there 
				are several trends, in particular, that have helped to spur this 
				evolution, including:<br>
				<br>
				An increased ability for business users to lead and make 
				technology decisions, with or without IT A continued CIO view 
				that IT strategies must be closely related to the business’s 
				strategic priorities Increased management pressure on EA to 
				demonstrate business value, due to the economic downturn 
				Continued growth and emergence of several new practices and 
				disciplines that are focused on enabling EA practitioners to 
				better communicate and collaborate with business leaders.<br>
				<br>
				“Enterprise architects need to assess their own aspirations, 
				their senior management’s expectations for EA and the type of IT 
				service management (ITSM) they are operating under to understand 
				the opportunities and constraints on their ability to 
				collaborate with business leaders,” says Philip Allega, research 
				vice president at Gartner.<br>
				<br>
				Given the current focus for EA, organisations will increasingly 
				support their EA efforts with people reporting into different 
				parts of the organisation (business and IT). Gartner analysts 
				say that a virtual reporting structure is fine, as long as team 
				members are all working together, based on a shared vision and 
				charter and are being measured based on this shared vision. It 
				is important to have one person working as the chief architect – 
				guiding, facilitating and leading the virtual team on a 
				coordinated effort. <br>
				<br>
				“If your organisation is still thinking about EA as an IT-only 
				discipline or developing an IT-focused guidance, you are in the 
				minority of your peers. If you are not actively seeking to 
				engage with business leaders and focusing EA on your business 
				strategy, you are in the minority of your peers,” adds Burton.
				<br>
				<br>
				The Gartner Enterprise Architecture Summit brings together a 
				large number of chief enterprise architects and it is the 
				premiere community gathering for enterprise architects, 
				strategists and planners. Designed to meet the needs of 
				enterprise architects, strategists, planners and innovative 
				specialists, this year’s summit helped EA leaders to solidify 
				fundamentals, redefine parameters and ensured stakeholder 
				support – important first steps towards managing the evolving EA 
				discipline with clarity <br>
				and confidence.<br>
