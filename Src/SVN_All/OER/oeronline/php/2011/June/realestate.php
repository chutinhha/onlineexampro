						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">
						Lack of maturity</font></b><p class="style15">
				<strong><span class="style14">An unwillingness to pay for 
				intellectual capital is a shortsighted approach that can cost 
				businesses dearly</span></strong><p align="justify" class="style13">
				There is a tendency in the developing countries not to pay for 
				intellectual capital but to pay only for physical capital. For 
				example, businessmen are willing to sink in millions of dollars 
				in putting up a factory but are unwilling to pay a few thousand 
				dollars to a consultant who can advise them if it is the right 
				technology, the right location, the optimal scale, the best 
				vendor etc. The result is that such investments are sub optimal 
				and in some cases the whole business goes bankrupt. <br>
				<br>
				This can be seen in real estate development too. Developers 
				often use poor quality architects and give design briefs that 
				are far removed from what the market wants. We have had 
				instances of developers planning an investment of a couple of 
				million rials but refusing to pay a little for a feasibility 
				study by a reputed real estate consultancy. The developer’s only 
				criterion is cost. Architects, chosen on the basis of low cost, 
				design poor quality buildings with sub optimal structures. 
				Similarly, developers give design briefs from their 
				understanding of the market and without using the services of a 
				real estate consultancy. The developers do not realise that that 
				is costly as the investment ends up giving poor returns. <br>
				<br>
				Good real estate consultancies contribute by advising developers 
				on the best mix, on the optimal size of units, finishes etc. 
				This is absolutely critical. In extreme cases we have advised 
				clients not to develop anything on a particular plot but sell 
				the land or just keep it for developing at a later date.<br>
				<br>
				A small or medium size development will take two to three years 
				from concept to completion. Unfortunately, developers make 
				investment decisions based on present returns with no thought to 
				how the feasibility and returns will change by the time the 
				development comes on stream two to three years later. As it is 
				widely known, real estate in Sohar is in distress. Most of the 
				development in Sohar has come up without any feasibility studies 
				and was unwarranted. Similarly a lot of office space is being 
				built in Muscat with no demand projection and a lot of them are 
				going to be vacant for long periods. This could have been easily 
				avoided with proper feasibility studies. <br>
				<br>
				Even when a development is profitable, one comes across many 
				cases where retail is built when there is no retail potential in 
				that location, the sizes of the apartments are too large and 
				hence the rent per square metre or sale price per square metre 
				is sub optimal, the finishes are not in consonance with the 
				positioning and the positioning is itself inappropriate to the 
				location. A good real estate consultant will, at minimal cost, 
				help a developer avoid these pitfalls. <br>
				<br>
				Some developers believe that good and expensive architects and 
				real estate consultants should be used only for premium 
				projects. This is totally untrue and a good architect and a good 
				real estate consultancy can make an enormous contribution even 
				to a project targeting the middle and lower income 
				buyers/tenants.<br>
