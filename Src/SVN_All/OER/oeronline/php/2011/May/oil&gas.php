						<span class="style27"><strong>STOKING INFLATION; CHOKING 
						GROWTH</strong></span><p class="style15">
				<strong><span class="style14">Rising crude prices pose a serious 
				challenge to curb inflation and may impact economic growth 
				adversely. Sunil Fernandes reports<br>
				</span></strong><p class="style15" style="text-align: center">
				<img border="0" src="images/oil&gas1-may2011.jpg" width="400" height="260" style="border: 1px solid #808080"><p align="justify" class="style13">
						<p align="justify" class="style13">
				The last few months have been characterised by unprecedented 
				developments in the oil and gas markets. First, it was the 
				unrest in the Middle East and North Africa region, followed by 
				the tsunami in Japan. Crude prices (WTI) have been gaining 
				ground steadily and have risen by as much as 50 per cent since 
				February 2011. Brent Crude has hit a recent high of almost $126, 
				breaching the highest levels achieved in the last 30-months. <br>
				<br>
				The sharp rise in crude prices is now a big worry. The global 
				economy would clearly be impacted by rising crude prices on 
				inflation, particularly in China and India, which have emerged 
				as giant economies. China is doing its utmost to dampen 
				inflation and has raised key interest rates earlier this month – 
				doing so for the fourth time since October 2010. <br>
				<br>
				India is already feeling the pinch of rising crude prices. 
				“Current oil prices will spur global inflation and retard 
				economic growth. India feels that this is a concern that needs 
				to be addressed on priority,” minister of state for petroleum 
				and natural gas R P N Singh said at the recently concluded 
				fourth Asian Energy Ministerial Round Table in Kuwait.<br>
				<br>
				His concerns can well be understood, since India faces a 
				precarious position. Should it continue to subsidise oil prices, 
				the country loses heavily, adding to the budgetary deficit which 
				is already running high. On the other hand, should it increase 
				fuel prices to factor in rising crude prices, the country stokes 
				up inflation, which is running at near 9 per cent levels. The 
				country has already raised rates in January this year, and it 
				was for the seventh time it did so, since raising rates in March 
				2010. <br>
				<br>
				The International Energy Agency (EIA) sees the impact of rising 
				crude running across economies. “Oil is harming global economic 
				growth and is a mounting concern for consuming nations,” Richard 
				Jones, deputy executive director of the International Energy 
				Agency said recently. <br>
				<br>
				“Oil at $120 or more has an effect on economic activity. We have 
				seen similar levels during times of economic slowdown if not 
				recession,” he observed. <br>
				<br>
				The moot question now is: Would crude prices dip? In the short 
				term, it looks unlikely that there would be a sharp reversal in 
				trend. There seems to be a sustained economic recovery around 
				the globe and a recent government report in the US showed that 
				gasoline inventories plunged the most in 12 years. Also, there 
				seems to be no end in sight to the present crisis in Libya, with 
				production having dwindled to a few thousand barrels per day.
				<br>
				<br>
				Having said that, it is also pertinent to point out that th ere 
				has been an element of speculation which led to a sharp rise in 
				prices. With some signs of stability, it is likely that 
				speculative unwinding may see prices retracing from the current 
				levels. <br>
