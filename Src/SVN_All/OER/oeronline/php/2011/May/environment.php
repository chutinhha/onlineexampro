						<span class="style27"><strong>INSPIRING TO THINK GREEN </strong></span><p class="style15">
				<strong><span class="style14">Oman Green Awards 2011 will take 
				the environment message several milestones ahead to reach and 
				connect the people of Oman in the green loop<br>
&nbsp;</span></strong><p align="justify" class="style13">
				<font face="Verdana" size="1">
				<img border="0" src="images/environment01-may11.jpg" width="150" height="140" style="border: 0px solid #808080; " align="left"></font><font color="#009933"><b>Oman 
				Green Awards (OGA), </b></font>Oman’s first environmental 
				initiative to recognise corporate and individual endeavours 
				aimed at driving home the importance of environment 
				conservation, is all set to present its second edition on June 
				5, 2011. <br>
				<br>
				OGA 2011 which follows the highly successful launch of the 
				annual event in 2010 has taken on an all-new portfolio of 
				generating a whirlpool of awareness among the public who can, in 
				little ways, make a big difference to the very concept of 
				environment conservation. It exhorts every member of the society 
				to digest the elements of conservation and pledge their might by 
				inculcating into their regime simplest of household tasks, like 
				switching off electricity when not in use, reducing water 
				consumption, etc. As a response initiative by OER, OGA has at 
				its core positive vibes of several like-minded corporate houses 
				and individuals who believe ‘green’ is the only way forward to 
				realise the ideals of environment conservation. It connects 
				people who have mooted green ideas and have individually made a 
				difference by forming a vital link in the awareness drive.<br>
&nbsp;<br>
				The new age mantra of environment conservation can become a 
				possibility only when every individual categorically makes a 
				behavioural change; one that gives environment a chance at every 
				platform. OGA has been created to awaken the eco-consciousness 
				in every Omani citizen and resident, with the objectives of 
				motivating behavioural change and increasing awareness in 
				relation to the protection and preservation of the environment.<br>
				<br>
				This major responsibility undertaken by OGA has won it kudos 
				from every quarter, including the United Nations Environment 
				Programme, which has acknowledged its efforts in connection with 
				the World Environment Day with a certificate of appreciation.<br>
				<br>
				The various segments included in the awards were adjudicated by 
				a panel of experts who based their decisions on effectiveness, 
				award categories innovation and creativity, impact, originality 
				and leadership and continuity and sustainability of the 
				initiatives undertaken by businesses, institutions and 
				individuals in promoting and implementing environmental 
				programmes. <br>
				<br>
				Within a span of a year OGA has become the ultimate recognition 
				for companies, institutions and individuals in Oman, who 
				demonstrate a commitment towards responsible consumption and 
				sustainable growth. The initiative honours and celebrates their 
				vision in leading the way to a cleaner, greener tomorrow. Its 
				three-fold objectives focus on boosting local environmental 
				efforts and achievements; providing green groups a platform for 
				concerted action; and raising awareness and generating greater 
				action. The event this year will take the environment message 
				several milestones ahead to reach and connect the people of Oman 
				in the green loop. The coming weeks will propel organisations 
				and individuals to think green and keep the environmental torch 
				burning. OGA 2011 has invited individuals and organisations to 
				register their nominations. <br>
