						<p align="justify" style="margin: 3px 10px"><strong>
						<span class="style27">POTENT RELATIONS</span></strong><p class="style15">
				<span class="style14"><strong>Germany offers a number of unique 
				business opportunities in diverse fields for Oman to capitalise 
				upon, says HE Angelika Storz-Chakarji, German Ambassador to 
				Oman, in a candid chat with Visvas Paul D Karra</strong></span><p class="style15" style="text-align: center">
				<font face="Verdana" size="1">
				<img border="0" src="images/face2face01-may11.jpg" width="523" height="163" style="border: 1px solid #808080; "></font><p class="style15">
				<br>
				Her Excellency Angelika Storz-Chakarji, German Ambassador to 
				Oman has lived history. And she feels very elated about being a 
				witness to the fall of the Berlin Wall in 1989 and the 
				subsequent unity of the German people, because rarely do people 
				get an opportunity to be not only a part of a historic moment 
				but also to be in the thick of action in an event of such a 
				magnitude as the unification of a country.<br>
				<br>
				As a young diplomat in the German foreign service, HE Angelika 
				had just returned from a posting at the United Nations 
				headquarters and had been assigned to the unit in the political 
				department which dealt with questions of the unity of Berlin and 
				of Germany as a whole. And so she was closely involved in the 
				negotiation process in that important year. The whole team was 
				heavily involved with the transition process dealing with border 
				issues with all the Eastern Bloc countries.<br>
				<br>
				<b><font size="2">Dream come true<br>
				</font></b>“I think this is a diplomat’s dream come true. Very 
				rarely you will get to live history. Sometimes when I look at 
				it, even though it was more than 20 years ago, it still feels 
				like yesterday,” says the ambassador.<br>
				<br>
				Recalling that period, HE Angelika says everyone was taken by 
				surprise by the turn of events as nobody had blueprints in the 
				drawer for this transition process. Everything had to be decided 
				at a fast pace. The government also realised that it was a 
				window of opportunity and the changes, (the fall of the Berlin 
				Wall and the unity of Germany) happened in a peaceful manner.<br>
				<br>
				HE Angelika, who hails from that part of Germany which in those 
				days was known as Western Germany, says that a united Germany 
				has since then built itself up as a global economic powerhouse.<br>
				<br>
				Today, when you talk of Germany, you will think efficiency, 
				excellence, quality and innovation. Germany is a technological 
				leader with big names in machinery, industry, engineering, solar 
				energy, and automobiles. Those were always the strengths of the 
				country but apart from that, there are other areas like 
				education, culture, scientific research etc, which offer a 
				diverse picture of Germany, HE Angelika points out.<br>
				<br>
				<b><font size="2">Enjoyable stint<br>
				</font></b>HE Angelika is quite pleased with her posting in Oman 
				as it is her first foreign posting as an ambassador to a country 
				and also her first posting to the Arab world as a diplomat. She 
				came to Muscat in July 2009 moving from Berlin. “I was 
				relatively a long time in Paris, then in Bonn and later in New 
				York at the permanent mission to the United Nations and in Tokyo 
				as well. I am really enjoying the stint as this region is an 
				interesting and fascinating part of the world,” says HE 
				Angelika.<br>
				<br>
				The German ambassador has had her fair share of excitement and 
				challenges in Oman and says it is difficult to highlight any 
				single event or thing but there is a lot of potential to 
				strengthen Oman-German relations. “For me, the important thing 
				is that the relations between the two countries especially 
				between the people should get more impetus. We had a German 
				minister of health visiting Oman two months ago and it proved to 
				be successful visit. The health sector is an area in which we 
				can work together. Such visits are important for us,” HE 
				Angelika says.<br>
				<br>
				When pointed out that German precision has not percolated to 
				Oman in terms of investments, HE Angelika says that the biggest 
				German investment in the Arab peninsula is in Oman namely, the 
				$800mn Oman Methanol Company. “We also have Siemens and 
				Oiltanking Odfjell Terminals &amp; Co etc. Besides this, there are 
				also a number of joint partners and I would say a big number of 
				hidden champions as well. Sometimes they are small but not in 
				that sense because very often they are in the field for a long 
				period of time however they are not known like the other big 
				names,” she says.<br>
				<br>
				<b><font size="2">Growing trade<br>
				</font></b>Bilateral trade figures issued by the German Central 
				Statistics Office show that German exports to Oman in 2010 was 
				€546mn, an increase of €100mn over 2009 with the main exports 
				being vehicles and machinery. The German imports from Oman was 
				pegged at €18mn in 2010 as compared to €16mn in 2009, with the 
				primary exports from Oman being agricultural products.<br>
				<br>
				What is relatively important is bilateral agreement on promotion 
				and protection of investments signed between the two countries. 
				According to HE Angelika this is a big step for boosting the 
				commerce as it will cover further activities.<br>
				Among other bilateral activities, a pilot project for training 
				Omani teachers to become teachers of German language is in the 
				early stages of being implemented. The embassy is proud of this 
				seeing it as a big step forward in furthering the educational 
				ties. On the educational front, GUTech University has been 
				playing an important role showcasing German excellence in a 
				number of fields. The university is concentrating on courses 
				like architecture, urban planning, engineering and other 
				courses, which fit the Omani educational curriculum.<br>
				<br>
				<b><font size="2">Health MoU<br>
				</font></b>The memorandum of understanding (MoU) on health is a 
				landmark tie-up, says HE Angelika, as there is a vast area of 
				cooperation for the two countries. Not only is the German 
				medical field very competitive, the services are renowned and 
				there are a number of Omanis who go to Germany for healthcare 
				services.<br>
				<br>
				The health minister was accompanied by a business delegation of 
				medical suppliers and producers of medical surgical equipments. 
				Germany has a number of clusters where medical equipments are 
				manufactured by small and medium enterprises which produce 
				highly technical medical surgical equipments.<br>
				<br>
				<b><font size="2">Outlook<br>
				</font></b>Talking about how the German and Oman bilateral ties 
				would shape up in the future, the Embassy is keen to encourage 
				the development of the SME sector in Oman, which is a major 
				focus area for the Sultanate. Says HE Angelika, “Encouraging 
				this vital sector is extremely important because, usually, 
				people think of big name and companies but economies worldwide 
				are run by SMEs. It is important for any society to strengthen 
				and develop this area and we feel there is room for 
				cooperation.”<br>
				<br>
				The renewable energy sector is another focus area for German 
				companies as Oman seeks various means to boost its power output 
				to cater to the growing need for electricity. Within the last 
				one year, there has been an increasing number of German firms 
				related to renewable energy which have been visiting Oman in 
				coordination with the embassy or in liaison with the Oman 
				Chamber of Commerce and Industry. A number of seminars have also 
				been organised and the embassy is closely involved in this by 
				way of handholding.<br>
				<br>
				HE Angelika also foresees potential in the higher education 
				because of the success of Muscat-based GUTech University, which 
				is unique in this region. Oman’s young population is a great 
				asset to the country and this gives rise to a lot of scope for 
				training and vocational education. The German side feels it is 
				important for them to show that they can cooperate with Oman in 
				this sector.<br>
				<br>
				On the personal front, HE Angelika’s husband is Syrian-Lebanese 
				and therefore she assumes the last name Chakarji, which means 
				sugar. She has two grown up children living in Berlin while her 
				husband runs a telecommunication business. She studied law and 
				international relations and added two years of more studies in 
				John Hopkins University. English was her first foreign language, 
				apart from Latin, which she learnt in school. She also speaks 
				French and a smattering of Italian.</td>
