						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">
						Qatar’s flying economy<br>
						</font></b><p align="justify" class="style13">
				<strong><span class="style14">Qatar’s per capita income could 
				reach as high as $100,000 this year, second to none in the world<br>
				</span></strong><br>
				<img border="0" src="images/closeup01-may11.jpg" width="523" height="347" style="border: 1px solid #000000"><p align="justify" class="style13">
				All the news coming from Qatar give some positive signals about 
				the prospects of Qatari economy in the near future. The country 
				has recently unveiled its first ever five-year development plan 
				for the period 2011-16 and the basic figures relating to fiscal 
				year 2011-12. Various credible sources also depict a rather rosy 
				picture of economic growth in Qatar.<br>
				<br>
				<b>First five-year plan<br>
				</b>Recently, Qatari authorities uncovered the country’s 
				first-ever plan for 2011-2016 in pursuit of attaining Vision 
				2030 goals. Other development plans are likely to come whilst 
				the country makes efforts to reach goals exhibited in Vision 
				2030.<br>
				<br>
				Broadly speaking, the plan is noted for projecting a notable 
				investment figure in excess of $125bn, carried out by the public 
				sector together with establishments in which the government owns 
				controlling stakes. The sizable investment spree is part of the 
				efforts for developing the necessary infrastructure for World 
				Cup 2022 to be held in Qatar.<br>
				<br>
				<b>Fiscal year 2011-12 <br>
				</b>Projected spending for fiscal year 2011-12, which commenced 
				in April, amounts to $38.5bn, up by 19 per cent compared to the 
				previous fiscal year. Amongst others, the new figure confirms 
				steady continuation of stronger spending years. The estimated 
				spending for fiscal years for 2010-11 and 2009-10 amounted to 
				$32.4bn and $26bn respectively.<br>
				<br>
				With regards to treasury income, the budget puts projected 
				revenues at $44.6bn. showing a hefty 27 per cent growth rate. As 
				such, the projected surplus amounts to $6.1bn. Yet, the budget 
				has the opportunity of either posting stronger surplus or 
				increasing spending, and yet probably both concurrently.<br>
				<br>
				The expectations relate to possible developments in the energy 
				sector, notably oil price. The Qatari authorities used average 
				oil prices of $55 per barrel in calculating oil revenue for 
				fiscal year 2010-11 ending in March, considerably below 
				prevailing market rates. Moreover, press reports project 
				continuation of a relatively low figure when Qatar releases the 
				budget for fiscal year 2011-12 starting in April, reflecting an 
				age-old conservative policy. By one account, the average oil 
				price could hover around $55 to $60 per barrel for the new 
				fiscal year.<br>
				<br>
				<b>Rapid growth <br>
				</b>Credible economic sources disagree on the extent but not the 
				ability of Qatar’s economy to register steady growth rates. For 
				instance, the International Monetary Fund (IMF) put Qatar’s 
				gross domestic product (GDP) growth at 16 per cent in 2010 only 
				to raise it to 20 per cent in 2011. Similarly, a recent report 
				issued by Samba bank of Saudi Arabia projects Qatar’s GDP growth 
				at 19 per cent for 2011, up from 16 per cent in 2010. Joyfully, 
				all these figures are in real terms, meaning adjusted for 
				inflation. Fortunately, inflationary threats are not substantial 
				nowadays notwithstanding rising oil prices.<br>
				<br>
				In fact, inflation was a real problem in 2007 and part of 2008 
				at the height of rising oil prices together with the declining 
				value of the American currency. Oil prices reached a record $147 
				per barrel in July 2008 only to drop sharply as the global 
				financial crisis began to unfold. However, changing 
				circumstances relating to the global financial crisis and new 
				insights gained on the way have helped keep inflationary 
				pressures in check.<br>
				<br>
				<b>Hard facts<br>
				</b>Undoubtedly, the positive economic prospects are based on 
				solid ground, particularly developments related to the energy 
				sector. Oil prices remain strong partly as a consequence of 
				socio-political developments in the Mena region.<br>
				Concurrently, Qatar continues to expand gas output, with 
				capacity already reaching 77 million tonnes per annum of 
				liquefied natural gas, up from 54 million tonnes only last year. 
				In reality, Qatar is the world’s leading exporter of LNG much 
				like Saudi Arabia with regard to crude.<br>
				Qatar’s gas sector is boosted by hard facts. According to the BP 
				Statistical Review of World Energy, Qatar’s gas reserves amount 
				to 25.5 trillion cubic metres, comprising nearly 14 per cent of 
				proven global reserves. Only Russia and Iran have more gas 
				reserves - 23.4 per cent and 16 per cent of global reserves, 
				respectively.<br>
				<br>
				Steady economic growth rates have helped Qatar rise to third 
				position in terms of the largest GDPs within the Gulf 
				Cooperation Council after Saudi Arabia and the UAE but ahead of 
				Kuwait. The GDPs of Qatar and Kuwait are pegged at $128bn and 
				$117bn, respectively.<br>
				<br>
				A recent IMF report suggests that Qatar’s per capita income 
				could reach as high as $100,000 this year, second to none in the 
				world. Second-place Luxembourg will report around $82,600 per 
				capita income. Per capita income in fellow GCC member states is 
				nowhere close to that of Qatar.<br>
				<br>
				<b>Proliferation of benefits<br>
				</b>Happily enough, Qatar’s economic prospects remain solid 
				until after the country has hosted the World Cup in 2022. The 
				positive outlook chiefly reflects planned spending of billions 
				of dollars on infrastructure development including a railway 
				system and road network as well as football stadiums whilst 
				preparing the country for the football extravaganza.<br>
				<br>
				The tournament has injected fresh life to the construction of a 
				causeway linking Qatar and Bahrain. The project is expected to 
				cost around $4bn. Local, regional and international investors 
				are expected to pour large amounts of money into Qatar’s economy 
				as the country prepares to host the tournament. Planned spending 
				schemes relate to construction of numerous projects within the 
				hospitality industry, as tens of thousands of visitors are 
				expected to come to Doha.<br>
				<br>
				Foreign residents and workers who make up around 80 per cent of 
				Qatar’s population benefit from the country’s growing economic 
				fortunes in terms of job opportunities and outgoing remittances. 
				Clearly, Qatar stands out when it comes to sharing the rewards 
				of growth with others.<br>
