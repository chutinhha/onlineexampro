						<span class="style27">FOR WELFARE’S SAKE</span></strong><p class="style15">
				<span class="style14"><strong>The revised State General Budget 
				for 2011 earmarks RO1bn in additional funding for welfare 
				measures such unemployment payouts, cost of living allowances, 
				enhanced pension and social security payouts. A report by <i>
				Muhammed Nafie<br>
&nbsp;</i></strong></span><p class="style15" style="text-align: center">
				<font face="Verdana" size="1">
				<img border="0" src="images/inthenews1-may2011.jpg" style="border: 1px solid #808080"></font><p align="justify" class="style13">
				In the previous issue, Oman Economic Review rightly observed (A 
				Crippling Burden, April 2011) how the Royal Directives 
				announcing various social welfare measures in the aftermath of 
				recent unrest in the country had placed an enormous and 
				unexpected financial burden on both public and private sectors. 
				OER’s findings were corroborated by the revised State General 
				Budget for 2011 which was recently unveiled by Minister 
				Responsible for Financial Affairs, HE Darwish bin Ismaeel al 
				Balushi, earmarking RO1bn in additional funding towards 
				employment generation, unemployment pay-outs, cost of living 
				allowances, enhanced pension and social security pay-outs, among 
				other welfare benefits. <br>
				<br>
				The enhanced burden would translate to an expected increase in 
				the projected budget deficit of RO850mn for 2011. As a result of 
				the new financial commitments by the government, public 
				expenditure will burgeon to about RO9.1bn during fiscal 2011, up 
				from RO8.1bn announced in the State Budget unveiled at the start 
				of the year. Consequently, the budget deficit is set to rise to 
				RO1.9bn, compared with a previously estimated deficit of 
				RO850mn, says HE Darwish.<br>
				<br>
				Elaborating on the details of the additional expenditure, HE 
				Darwish says RO444mn is allocated for providing employment to 
				40,714 people, RO90mn for unemployment allowance, RO43mn for 
				pension of social security households, RO89mn for pension for 
				retired civilian and defence employees, RO251mn for cost of 
				living for employees in military and civilians, RO25mn for 
				students allocation and fare for schools bus, RO25mn for pension 
				for Omanis in private sector and RO14mn for support to sports 
				and youth sector. <br>
				Al Balushi also stresses that the RO1bn in additional funding 
				will largely come from oil revenues earned above the $58 per 
				barrel benchmark set by the government in formulating the 2011 
				State Budget. OER had perceived that with crude prices hovering 
				at the $100 per barrel mark, Oman should comfortably end up with 
				a surplus budget despite the additional expenditure. <br>
				<br>
				“The budget deficit is expected to be offset by higher oil 
				revenues expected to range from $75–80 this year. Consequently, 
				the government will neither have to dip into its financial 
				reserves, not resort to any borrowing, says HE Darwish.<br>
				<br>
				In his address, HE Darwish also underlines the government’s 
				commitment towards the execution of developmental projects set 
				out in the approved 8th Five Year Development Plan 2011-2015. Of 
				the RO12bn in allocations towards projects outlined in the Plan, 
				RO6.4bn is earmarked for ongoing ventures launched during the 
				previous plan. The balance RO5.6bn is the outlay towards new 
				projects listed out in the 8th Plan.
						<hr color="#C0C0C0" size="1" width="95%">
						<p align="center" class="style13">
				<font color="#0000FF"><b><font size="2">BUSINESS LEADERS,YOUNG ACHIEVERS HONOURED</font><br>
&nbsp;</b></font><p align="center" class="style13">
				<img border="0" src="images/inthenews2-may2011.jpg" style="border: 1px solid #808080"><p align="justify" class="style13">
				<br>
				<b><span style="font-size: 9pt">Now into its sixth consecutive 
				year, BizPro Business Leader Awards 2011 has become a benchmark 
				for recognising and rewarding Omanis who have excelled in their 
				professional domains</span></b><br>
				<br>
				The BizPro Business Leader Awards 2011 were presented to 
				business leaders and young achievers at a gala function in the 
				presence of hundreds of luminaries from the corporate world, and 
				Khalid bin Sulaiman al Siyabi, the first Omani to scale Mount 
				Everest, as the guest speaker. More than 100 Young Achiever 
				nominees had been filtered down to 3 by the expert KPMG panel 
				that took them through the evaluation process. <br>
				<br>
				Emerging as winners in the Young Achievers’ category were Ammar 
				Sulaiman al Kharusi, supervisor at Omran; Aliya Abdullah al 
				Zadjali, C&amp;A engineer at Mott MacDonald and Company; and Khalid 
				Mohammed Ahmed al Barwani, Team Leader at Ahli Bank. The winners 
				will become role models and mentors for their peers and to the 
				younger generation. Samir J Fancy and Stephen R Thomas, Chairman 
				and CEO respectively of Renaissance Services, presented the 
				awards to the winners. Each young achiever got RO2,500 as a 
				scholarship sponsored by Renaissance Services. <br>
				<br>
				Sayyida Rawan Ahmed al Said, MD and Group CE of ONIC Holding was 
				announced as the new Business Leader for the year 2011 by HE 
				Khalil Abdullah al Khonji, chairman, OCCI. Sayyida Rawan who is 
				the first Omani woman to hold the position of chief executive in 
				a publicly listed company holds an MSc in Economics and Finance 
				from Loughborough University, UK, a Post Graduate Diploma in 
				Investment Analysis from Stirling University, UK and a BA in 
				Economics and Political Science from the American University, 
				Cairo. She is on the board of a number of companies and 
				financial institutions in Oman and has over 23-year experience 
				in the field of investment. <br>
				<br>
				The main objective of these awards, now into the sixth 
				consecutive year, has been to reward and promote the 
				achievements of the Omani Business Leaders and Young Achievers 
				by acknowledging their contribution to the corporate sector in 
				Oman. The National Training Institute has instituted the BizPro 
				Awards to be a definitive thrust towards recognising the role 
				models and mentors of the business world. <br>
				<br>
				The Business Leader for 2011 and the three Young Achievers, who 
				were chosen from among six finalists in each category, made 
				individual presentations at the award ceremony to prove their 
				true ability to deserve this kind of prestige and honour. <br>
