				<font face="Verdana" size="1" class="style11" style="font-size: 11px">
				<b>THE BMW GRAN TURISMO COMBINES THE CHARACTERISTIC FEATURES OF 
				A PRESTIGIOUS SEDAN, THE FUNCTIONALITY AND SPACE OF AN SAV AND 
				ELEMENTS OF A TRADITIONAL GRAND TOURING CAR. A REVIEW BY MAYANK 
				SINGH</b><br>
				<br>
				The BMW Gran Turismo (GT) is an embodiment of the leading 
				premium automotive manufacturer’s commitment to innovation. The 
				exterior of the car combines the proportions so typical of a 
				BMW, an elegantly stretched silhouette and a coupé-like 
				roofline, as well as four doors with frameless windows to create 
				one complete, harmonious unit. <br>
				<br>
				The front view of the car animates powerful dynamism through the 
				sheer presence of the upright BMW kidney grille, large air 
				intakes and dual round headlights slanted back at an attractive 
				angle. For the first time the daytime driving light function is 
				provided by corona rings with LED technology, while the rear end 
				is enhanced by L-shaped rear light clusters with illuminated 
				rows of lights in LED technology and a two-piece tailgate.<br>
				<br>
				<font color="#FF0000"><b>LAP OF LUXURY </b></font><br>
				<br>
				The luxurious interior of the GT enables the driver and 
				passengers to vary the generous space inside the car to their 
				individual, personal requirements. This superior feeling of 
				comfort is further enhanced by the large window areas, the 
				optional panorama glass roof, as well as high-class materials 
				with supreme quality of finish. <br>
				<br>
				It’s the interior package that makes the GT unique. The front 
				seat H-point is about two inches higher than that of a regular 5 
				Series sedan, but almost four inches lower than that of an X5. 
				You slide in behind the wheel of the GT rather than sinking down 
				or stepping up to it, and get a better view of the road ahead. 
				BMW calls it a ‘semi-command’ driving position. <br>
				The long wheelbase and heavily crowned coupe roofline means 
				there’s plenty of leg and headroom, especially for rear seat 
				passengers. The standard rear seat slides 3.9 inches fore-aft, 
				and its backrest, which is spilt 40-20-40 is rake adjustable 
				from 15 to 33 degrees. An optional luxury rear seat package, 
				which also includes rear window sunshades and four-zone climate 
				control, offers accommodation for two passengers only and is 
				electrically adjustable.<br>
				<br>
				The really clever stuff -- and the key to the GT’s appeal -- is 
				the configurable load space behind the rear seats. Unlike most 
				hatchbacks, wagons, and SUVs the GT features a variable 
				partition between the cabin and the load space, topped by a 
				solid parcel shelf edged with a stout rubber seal. In regular 
				mode the partition and parcel shelf offer the silence and 
				security normally associated with a regular sedan or coupe. <br>
				<br>
				But with the parcel shelf removed the partition can be moved 
				through the same arc as the rear seats. The GT enters the market 
				carrying over BMW EfficientDynamics into a new segment in the 
				automobile world, allowing the model to set new standards in 
				terms of economy and emission management. <br>
				<br>
				<font color="#FF0000"><b>TOP OF THE RANGE</b></font><br>
				<br>
				The GT is available in the Oman market with eight-speed 
				automatic transmission and two engine variants – the in-line six 
				cylinder 35i and the V8 50i – both with twin-turbo technology. 
				The BMW 35i GT has a maximum output of 306 hp with a top speed 
				of 250 km/hr and acceleration from 0–100 km/h in 6.3 seconds. 
				The top-of-the-range engine, the BMW 550i GT has a maximum 
				output of 407 hp with a top speed of 250 km/hr and acceleration 
				from 0–100 km/h in 5.5 seconds.<br>
				<br>
				A host of safety and comfort options are available on the model, 
				including Integral Active Steering to improve manoeuvrability 
				and safety at low and high speeds, Driving Dynamic Control for 
				variable drive settings depending on road conditions and 
				adaptive headlights - a function that varies the angle of the 
				headlight beam when it detects a change in direction. Active and 
				passive warning systems such as the Lane Change and Lane 
				Departure Warnings ensure that the driver stays within lane 
				limits. <br>
				<br>
				The GT also comes with a wide range of comfort touches and 
				innovative entertainment options, including telecommunication 
				and navigation systems, a multi-channel audio system, a DAB 
				double tuner, USB port, 80 GB Hard Disk, DVD entertainment 
				system in the rear, four-zone automatic air conditioning, and a 
				panorama glass roof. Overall the GT is a complete family vehicle 
				that combines innovation and elegance in equal measure.<br>
