						CHANGING THOUGHT PROCESSES</font></b><p align="justify" class="style3">
				<span class="style5"><strong>FOOLED BY RANDOMNESS PUTS FORTH A 
				CASE FOR APPRECIATING THE ROLE OF LUCK IN LIFE AND KEEPING AWAY 
				FROM OUR PROPENSITY TO CONFUSE FORTUNE WITH REASON. AN OER 
				REVIEW<br>
				</strong>
				</span>
				<br>
				<font face="Verdana" size="1">
				<img border="0" src="images/browsing01-may11.jpg" width="238" height="342" style="border-style:solid; border-color:#808080; float: right" class="style4"></font>Fooled 
				by Randomness is a primer on the obstacles that human beings 
				face while reasoning around probability. Taleb argues that human 
				beings are prone to misattribute the results of human endeavours 
				to skill and knowledge that are, in fact, just coincidental and 
				random events. <br>
				<br>
				A market trader and a professional skeptic, Taleb claims to be a 
				mathematical novice, but he is clear on one thing: the 
				importance of understanding the structure of random events, 
				their significance and, especially, insignificance. He clearly 
				sees that this understanding is more important than actual 
				calculations as he has seen innumerable traders - “blow up”, in 
				the picturesque jargon of the trade - when a seemingly 
				successful career is brought to a spectacular end by some 
				“unexpected” market collapse. <br>
				<br>
				There are many ways of being the fool of randomness. One, as 
				here, is to fail to predict the rare event. Nothing can be more 
				certain than that the unexpected will happen sooner or later, 
				but lulled into a sense of security by the periods of relative 
				calm in between, people forget to allow for it. Another is to 
				see significance in some random pattern. Taleb explains with 
				crystal clarity why the more often you look at some fluctuating 
				quantity (the value of your share portfolio, for example), the 
				less meaning your observations have. Yet he sees traders who 
				watch prices move up and down in real time on screen - the 
				changes are so small as to be completely random - and think they 
				are learning something.<br>
				<b><br>
				Rose tinted glasses</b><br>
				Taleb’s argument is that people are often tricked, mainly by the 
				architecture of their own brains, into thinking that things that 
				happen at random are actually happening by design. Adam Smith, 
				the great Scottish economist and philosopher, wrote more than 
				two centuries ago of “the overweening conceit which the greater 
				part of men have of their own abilities [and] their absurd 
				presumption in their own good fortune.” Many investors and 
				business executives who think they are geniuses are merely the 
				beneficiaries of good fortune. “We tend to think that traders 
				are successful because they are good,” Taleb writes. But the 
				truth is that “one can make money in the financial markets 
				totally out of randomness.” <br>
				<br>
				Another, trend that is the “survivorship bias”: in a random 
				population, some items will be more visible than others.<br>
				<br>
				The lesson here for investors is powerful and frightening. How 
				much can you rely on the track records of investment advisers, 
				mutual fund managers, newspaper columnists, or even the market 
				as a whole in making decisions about your investment portfolio? 
				Not nearly as much as you probably think.
