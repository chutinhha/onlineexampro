						<span class="style27"><strong>EDUCATION IS THE ANSWER</strong></span><p class="style15">
				<strong><span class="style14"><font color="#808080"><i>Abdulaziz 
				A Al-Sowailim, Chairman and CEO, Ernst&amp;Young Middle East</i></font> 
				feels that there has to be a better connect between the 
				requirements of the private sector and what is being produced by 
				educational institutions in the region. Mayank Singh reports<br>
				</span></strong><p align="justify" class="style13">
				<font face="Verdana" size="1">
				<img border="0" src="images/intrvu1-may2011.jpg" style="border: 1px solid #808080; float: right;" class="style28" width="150" height="168"></font>Abdulaziz 
				A Al-Sowailim has been a member of the Executive 
				Committee/Management Team of E&amp;Y’s Middle East practice for the 
				past six years. After graduating from King Saud University, he 
				joined E&amp;Y’s Riyadh office. He served as the managing partner of 
				AlKhobar and Riyadh offices prior to assuming the responsibility 
				as the country managing partner of the Saudi practice. The Saudi 
				practice includes three major offices, Riyadh, Jeddah and Al 
				Khobar. It comprises over 1,000 professionals and 32 partners. 
				Historically, the Saudi practice represents almost one third of 
				the Middle East revenues and the number of professionals and 
				partners. <br>
				<br>
				Abdulaziz is a member of the American Institute of Certified 
				Public Accountants, Saudi Organisation of Certified Public 
				Accountants and is licensed to practice in the Kingdom of Saudi 
				Arabia. He is also a board member of the Saudi Organisation of 
				Certified Public Accountants which has a role similar to the 
				PCAOB in the USA. In addition, he has served as vice chairman of 
				the Saudi Accounting Association. Excerpts from an interview:<br>
				<br>
				<b>How has business been in the last one year for E&amp;Y in the 
				region?</b><br>
				I can look at business from various angles. The global economy 
				was going through a boom and the opportunity was there but when 
				the global financial crisis hit everyone got affected. We have 
				been lucky in this region as we were less affected by the 
				crisis. While we were not immune to the crisis, the measures 
				taken by various governments ensured that the impact of the 
				crisis was not anywhere close to what some other parts of the 
				world went through. A lot of people are in agreement that 
				globally we are witnessing a trend that can be summed up as – 
				LUV. The letter L demonstrates the shape of Europe as the 
				downturn there was sharp and is continuing. In the US, it is a U 
				– so while improvements are taking place they are taking a long 
				time to recover. The emerging markets in the GCC region 
				represent a V shape as the recovery is happening very fast. In 
				terms of business we have been affected but not much. At E&amp;Y we 
				looked at what was happening and decided to consolidate. As a 
				business we have the flexibility to adapt to new situations as 
				we are a completely networked organisation. We could mobilise 
				people from different sectors and industries and that was 
				helpful for us. We also worked with our clients to help and 
				support them wherever needed. <br>
				<br>
				<b>There have been concerns about what is happening in the 
				region lately in terms of protests, demands and agitations. What 
				are your views on the recent turn of events?</b><br>
				<br>
				It is very difficult to give one answer as it is not a 
				one-size-fits-all situation. We have seen what happened in 
				Tunisia, Cairo and Libya but these have to be viewed 
				individually in their context. In the GCC, there have been 
				demands for more transparency and jobs and governments are 
				sorting out the issue. I can see us coming out of this very 
				quickly, so I am not concerned about it. I recently read some 
				statistics about Spain which stated that 60 per cent of people 
				around the age of 25 are unemployed, compared to this we are 
				doing much better in the GCC region. <br>
				<br>
				<b>There are economic reasons underlying these protests – 
				whether it is the demand for more jobs, better wages or improved 
				working conditions. What can be done to address these concerns?
				</b><br>
				<br>
				The biggest focus for all governments needs to be education. The 
				private sector expects someone who would come in with a certain 
				amount of knowledge and expertise. Once this employee gets some 
				training he or she can starts delivering results. This requires 
				a better correlation between the demands of the job market and 
				what educational institutions are producing, I think that 
				currently there is a bit of a gap between the two and that needs 
				to be bridged. <br>
				<br>
				<b>Oil prices seems to be on fire breaking every resistance 
				level, while it may be good news for oil producing countries it 
				can adversely impact the global economy. What in your view would 
				be the ideal price level?</b><br>
				<br>
				It is difficult for us at this stage to know as to what levels 
				we are talking about, because the risk is still too high. Any 
				turbulence that happens in the region is going to inflate oil 
				prices. Saudi Arabia has announced that it will bridge the gap 
				if there is any shortage of oil. The governments in the region 
				are taking care that no major turbulence happens in respect to 
				oil production and prices and that should fix the situation. Oil 
				prices are rising and in the short term it would rise a bit 
				more, but if the political situation turns for the worse like 
				say something happens in Iran then it would hike oil prices 
				significantly. <br>
				<br>
				<b>Has the global financial crisis changed the way E&amp;Y did its 
				business and have clients started demanding new services?</b><br>
				<br>
				Clients are asking more about what they can do themselves, they 
				are also looking at risks both internal and external and ways to 
				improve corporate governance. These are two critical elements 
				that have come in because of what happened in the recent past. 
				They are also looking at reorganisation and streamlining their 
				businesses. The global financial crisis was a liquidity crisis 
				and professionals dealt with it very well. Regulators on their 
				part have started stressing on better risk management and that 
				augurs well for the future. <br>
				<br>
				<b>E&amp;Y’s Oman practice has been a leader in the market by a 
				stretch and you have restructured operations with Oman playing a 
				more prominent role in E&amp;Y’s practise in the region. Can you 
				take us through these recent developments? </b><br>
				<br>
				The Oman office was managed by one of our partners Philip 
				Stanton, when I took over the leadership of the MENA region as 
				the chairman in July 2010, I decided to make Stanton the COO and 
				because of the success that we are having in Oman, he continued 
				to stay in the Sultanate. Various MENA heads like people, 
				technology and finance are now located in Oman. Sridhar 
				Sridharan has been made the managing partner for the Oman 
				operations. We have also moved some of our functions from other 
				GCC countries to Oman. As we are the leading firm in Oman (and 
				in the region) in terms of manpower and operations, I am 
				spending a considerable amount of my time to support the 
				practice here and I am sure that it will only grow in future.
				<br>
				<br>
				<b>What are your plans for E&amp;Y in the region? </b><br>
				<br>
				A major part of our strategy is to focus on the National 
				Development Programme and this applies mainly to the Gulf 
				region. We are monitoring the growth of the nationalisation 
				programme in every country in the region. In Oman we have 65 
				Omanis out of which 25 are seniors and we are very proud of 
				this. We have a five year strategy in place up to 2015. Though 
				we are an international firm we operate like a local business. 
				We are pleased that our leadership teams in most countries in 
				the GCC comprises of capable and highly skilled nationals from 
				the country in which we operate. This is our contribution to 
				countries and the people. This has been a hallmark of E&amp;Y for 
				the last 25 years. This is the result of the training and 
				development opportunities that we give to our staff. <br>
				<br>
