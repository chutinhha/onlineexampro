		        <td valign="top" width="100%"> <p align="justify" style="margin: 3px 10px" dir="ltr"><font face="Verdana" size="1"> 
                    <br>
                    </font><b><font face="Verdana" size="4" color="#C00000">
				For fertile roofs<br>
                </font></b>
				<font face="Verdana" size="1">
				<br>
                </font><b><font face="Verdana" size="2">
                <img border="0" src="images/environmen-1.jpg" align="right" hspace="12" vspace="6" width="200" height="181" style="border: 1px solid #C0C0C0">SQU’s 
				‘roof-mounted irrigation by capillary siphoning’ is an effective 
				combination of green roofing and capillary siphoning 
				technologies<br>
                </font></b>
				<font face="Verdana" size="1">
				<br>
                Roof-mounted irrigation by capillary siphoning’ is a unique and 
				innovative project by Sultan Qaboos University (SQU) to study 
				the feasibility and technical applicability of green roofs with 
				a novel capillary siphoning irrigation system in Oman. It is 
				also aimed at assessing the durability of the siphons as water 
				conduits in harsh Omani conditions and studying the temperature 
				regimes of the roof near siphoned pots and the soil inside the 
				pots.<br>
				<br>
				As part of the project, different types of crops were cultivated 
				in pots filled with three types of soil (coarse sand, recharge 
				dam silt and soil from the SQU agricultural experimental station 
				plot). The pots were placed in an open area of the roof of the 
				university buildings. Water was diverted to the pots from 
				containers through siphons made of used T-shirts, which are 
				rolled and filled with soil. Siphoning flow rate, moisture 
				content in the pots, hydrological parameters and the development 
				of the plants were measured and monitored.<br>
				<br>
				The project also assessed the feasibility of further greening 
				the roofs of Omani houses and their passive thermal insulation. 
				Pot modules at the extreme temperature and dryness of Omani 
				climate showed to be efficient thermal insulators of the roof 
				and suitable biomedium for plants. In addition, regular 
				measurements of temperature, moisture content and electrical 
				conductivity of soil, flow rates of siphoning were carried out 
				to prove the agronomical and engineering feasibility of green 
				roof modules.<br>
				<br>
                </font><b><font face="Verdana" size="2" color="#000080">
				Alternative irrigation system</font></b><font face="Verdana" size="1"><br>
				<br>
				The project was also an attempt to explore and develop a novel 
				irrigation system. Siphoned pots serve as good buffers for 
				regular regional rainfalls. Irrigation by siphoning in regional 
				conditions with precautions on secondary salinisation is 
				recommended based on four different green roof modules tested. 
				Daily-average temperature of the roof is shown to be reduced by 
				6-8 degrees (in June) with mid-day temperature peaks reduced by 
				more than 20 degrees.<br>
				<br>
				Besides, improving building thermal regimes and flood 
				attenuation, it can utilise idle roof space for cultivating 
				basic vegetables in urban households, recycling used family 
				cloth and plastics and engaging family members in farming 
				activities. Several plants (ivy, sunflower, succulents and 
				others) are shown to grow well on the roof when the maximum 
				concrete/gravel temperature of the roof reaches more than 70 
				degree.<br>
				<br>
				The project has proved the effectiveness of the green roofs plus 
				capillary siphoning technology in the Sultaante and other Gulf 
				countries.<br>
