				<p class="MsoNormal" align="justify">
				<font face="Verdana" size="2"><b><br>
				A combination of talent and 
				single minded dedication has enabled </b><i>Tariq Al Barwani</i><b> 
				to become a role model for Oman’s youngsters. </b><i>Mayank 
				Singh </i><b>reports<br>
				<br>
				</b>
				</font><font face="Verdana" size="1">An IT wizard, business 
				executive, radio jockey, knowledge disseminator, a columnist and 
				a teacher – if you thought that we are alluding to a group of 
				people, just hold on to your horses. The person in question is 
				Tariq Hilal Al Barwani, head of corporate affairs, Nawras and 
				the president and founder of
				<a href="http://www.KnowledgeOman.com">www.KnowledgeOman.com</a> 
				. And Tariq does all of these and probably more. <br>
				<br>
				<br>
				</font><b><font face="Verdana" size="2" color="#000080">Early 
				awakening</font></b><font face="Verdana" size="1"><br>
				<br>
				The development programme of civil ministries and government 
				units represents one of the main executive tools to realise its 
				objectives, policies and implementation of projects included in 
				the successive Five-Year Development Plans. The cited plans were 
				formulated according to requirements and needs of each stage 
				after a thorough assessment of the previous Five-Year Plans. The 
				total government expenditure during the previous four decades 
				amounted to RO94bn taking in account equitable regional 
				distribution of the outcomes of the development. Services like 
				education, health, electricity, water, roads, communication were 
				spread in various governorates and regions of the Sultanate.<br>
				<br>
				Few would have guessed that a childhood passion for computer 
				games and cartoon characters would grow into a life time 
				penchant for information technology. Says Tariq, “It all started 
				with my parents buying me Atari, a video console game. I would 
				constantly think as to how I could be a person in the game or 
				create characters that are in the game.” This also encouraged 
				him to look at a character or problem from different angles and 
				to see as to how a problem could be solved – a trait that he has 
				nurtured over the last 31 years. <br>
				<br>
				At the age of five his parents bought him a home computer system 
				released by Sinclair Research Limited in the UK known as ZX 
				Spectrum 128. “My father told me, ‘you can use this to play 
				games or to create programmes and that’s where it all started.” 
				Hilal Al Barwani, his father, whom Tariq refers to, often, as a 
				mentor and friend, spotted his talents at an early age and 
				nurtured them. <br>
				<br>
				A private teacher was hired to take him through the paces with 
				computers. In 1989 at the age of 10 he was given a personal 
				computer (PC) to work upon. Says Hilal, “I noticed Tariq’s 
				talents at an early age because when I introduced him to a 
				computer device he was not merely interested in playing games as 
				most kids of his age would be but wanted to create games.”<br>
				<br>
				The reaction of people around him gave Tariq an important 
				insight into the future. “I saw that people around me were not 
				familiar with computers and IT but whenever I made a small 
				programme they would say ‘Wow’ and that gave me an idea that 
				these simple tools may become necessities for people in the 
				years to come.” He started out by creating small programmes and 
				anti-virus cleaners and since then, there was no looking back. 
				In the late 1990s he created a software for Omantel that helped 
				the company’s Internet subscribers to change from GTO to Omantel 
				seamlessly. “For them it was magic but for me it was science and 
				how I could use technology for the benefit of businesses and 
				people.”<br>
				<br>
				Realising the need for a more structured approach towards his 
				interest he did his diploma in information systems in Oman. He 
				went onto pursue a bachelors with an honours in computer science 
				from Canada’s Nova Scotia University and a Masters in IT from 
				Australia. He is currently doing his doctorate in management of 
				organisations from University of Phoenix in the US. <br>
				<br>
				</font><b><font face="Verdana" size="2" color="#000080">Good 
				grounding</font></b><font face="Verdana" size="1"> <br>
				<br>
				When he was just 18 years he was invited by United Media 
				Services (UMS) to join the company’s Internet cell (now known as 
				UMS Interactive). Says Tariq, “I received an e-mail from UMS 
				inviting me to join the company. The mail stated that they had 
				looked at my website and the company must have realised that 
				they could utilise some of its characteristics for their 
				business.” Then a high school student, he recalls going for the 
				interview accompanied by his father and his bag. He accepted the 
				offer and worked at UMS as a web programmer. “In two years, we 
				created numerous websites for a number of companies. I had the 
				expertise but I built the work experience at UMS. We created the 
				first general website called Omanaxis.com and I was an integral 
				part of the process,” he avers. His talent is corroborated by 
				Sandeep Sehgal, chief executive, UMS, “Tariq exhibited 
				extraordinary skills for someone of his age and we knew that he 
				was destined for bigger things in life. His perseverance, 
				energy, dedication and knowledge was exemplary and something 
				that others could learn from.” <br>
				<br>
				While working at UMS it occurred to him that he should create 
				something for Oman. This led him to create an IT portal called 
				Tariq.net which gave information related to information 
				technology. And all his programmes could be downloaded for free 
				from the portal. The website got an overwhelming response from 
				users and sponsors alike. Companies like BankMuscat, Commercial 
				Bank of Oman, The Bahwan Group and Khimji Ramdas supported the 
				site. As traffic on the site grew Tariq left UMS to concentrate 
				on Tarik.net. Soon he got a number of offers to buy the site and 
				finally sold it to the OHI Group. The company rechristened the 
				portal as OHITarik.net. He joined OHI as the youngest manager 
				and probably was the youngest IT manager in the country at that 
				time with a four figure salary. At OHI he built a team and 
				created a number of websites. After two years at his father’s 
				behest he left his job to pursue a masters degree. “When I left 
				OHI everyone thought that I was crazy,” recalls Tariq.<br>
				<br>
				Nawras the second mobile operator in the Sultanate was gearing 
				up to launch its operations and on his return from Australia, 
				Tariq joined it as an online manager in January 2005 looking 
				after their Internet presence. Tariq has been instrumental in 
				changing the structure of the Nawras website. Says Tariq, “The 
				Nawras website has been winning almost all the regional and 
				local awards since the company commenced operations.” Ross 
				Cormack, CEO, Nawras adds, “It is great to see how Tariq raises 
				the profile of communications and IT in the country and by 
				encouraging and energising the involvement of young people he 
				inspires others as well as raising the profile&nbsp; of Nawras.”<br>
				<br>
				After four years as the online manager Tariq was promoted as the 
				head of corporate affairs. In his new role he looks after media 
				relations, PR, sponsorships and events. In a nutshell – “it is 
				all about safeguarding the image of the company externally.” 
				Says Eric Kerboriou, marketing director, Nawras, “Tariq has a 
				very nice personality, he is pleasant at work and is always 
				willing to support when his natural interpersonal skills are 
				needed. He is a very positive person and when he commits to 
				delivering, he does it to the best of his abilities. At the 
				beginning of his new assignment managing the team and handling 
				the diversity of the role was a challenge for him but he handled 
				it very well.” <br>
				<br>
				</font><b><font face="Verdana" size="2" color="#000080">Social 
				conscience</font></b><font face="Verdana" size="1"><br>
				<br>
				In 2007, he crisscrossed the country over two weeks as a part of 
				an Information Technology Awareness journey. On his way from 
				Muscat to Salalah he visited a number of schools and colleges 
				educating people on the benefits of technology. The experience 
				inspired him to create something that could promote IT awareness 
				in the country. This led to a new portal KnowledgeOman.com (KO). 
				“The idea is to transform Oman into a knowledge-based society 
				and this is based on the vision of His Majesty Sultan Qaboos bin 
				Said.” KO is a community driven project with 66 volunteers who 
				are driven by the same passion and zeal. KO has over 8,000 
				members and is a growing community. Says Rym Aoudia, media 
				affairs, KO, “Tariq is a man who walks the talk, his skills and 
				understanding of individuals has enabled him to lead a team of 
				people from diverse backgrounds, age groups and nationalities to 
				work as volunteers for the community.” The portal has an online 
				and offline version. While the online version dessiminates 
				knowledge, KO volunteers work closely with charitable 
				organisations like Dar al Atta’a in various CSR initiatives. The 
				portal has been winning various accolades. It recently won the 
				Global Brand Leadership award at the World Brand Congress 2010, 
				for the community brand leadership. Says Khalfan Al Mahrazy, 
				vice president, KO, “Tariq is a thinker and doer. He always 
				thinks and talks about tomorrow and the future while he ensures 
				everything is at its best today. He knows what it takes to make 
				a difference.”<br>
				<br>
				Having been a newspaper columnist for a decade, he thought of 
				using another media – radio, to promote IT awareness in the 
				country. “I thought about creating a programme that would not 
				just be about entertainment but would spread knowledge.” This 
				led to the programme Tarik Talk on Oman Radio FM. The programme 
				focuses on different aspects of technology, personal development 
				and careers. The programme started as Cyber Talk two years back 
				and was later rechristened. <br>
				<br>
				Ask him about how he manages to keep himself abreast of the 
				developments in IT field and he avers, “I keep aside a few hours 
				for learning and upgrading my skills. I have a strong base and 
				so it is just a matter of updating myself. I believe that life 
				is like a treadmill and if one stops while the treadmill is 
				moving it will throw you away.” His hobbies include reading and 
				spending time with his four-year-old son Hilal and two-year-old 
				daughter Dania. His advice to youngsters – “Build up your 
				interest, work hard and listen to your parents.”</font></p>
