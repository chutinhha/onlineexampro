                  <p align="justify" style="margin: 3px 10px"> 
					<b><font face="Verdana" size="1" color="#C00000">&nbsp;</font><font face="Verdana" size="4" color="#C00000"><br>
					We are a destination developer<br>
					</font></b><font face="Verdana" size="1"><br>
					</font><font face="Verdana" size="2"><i>Adil Taqi</i><b>, 
					CEO, Muriya Tourism Development company speaks to </b><i>
					Mayank Singh</i><b> about the company’s differentiation, 
					project timelines and future plans in his first media 
					interview </b></font></p>
					<p align="justify" style="margin: 3px 10px"> 
					&nbsp;</p>
                  <p align="center" style="margin: 3px 10px"> 
					<img border="0" src="images/face2face-1.jpg" width="150" height="186" align="left" style="border: 2px solid #C0C0C0"></p>
                  <p align="justify" style="margin: 3px 10px"> 
					<font face="Verdana" size="1"><b>The real estate sector has 
					been going through a slowdown regionally. What made you take 
					up this job amidst such circumstances?<br>
					</b><br>
					Muriya Tourism Development is one of the companies that will 
					position Oman on the global map. Since Oman’s population is 
					only 1.8-1.9mn, there are structural limitations to growth 
					and this impacts traditional sectors like banking and 
					telecom. When you look at tourism, there are no such 
					limitations – your limitations in terms of tourism tend to 
					be around infrastructure, hotel rooms, airport size and 
					capacity and so forth, but these are not structural issues. 
					This is what attracted me to Muriya. Even before I went out 
					of Oman in 2006 we had been hearing this rhetoric about 
					Oman’s Tourism potential but I do not know as to how 
					genuinely people believed in it – I certainly felt that it 
					was more of a lip service. Since then a lot has happened. 
					When you go to a country like Seychelles, which is an 8 x 17 
					mile island you find that tourism is everything that they 
					do, everything else is peripheral and you see how the entire 
					value chain is structured towards tourism. The beach strip 
					at the Banyan Tree Resort in Seychelles is very ordinary 
					compared to the beaches of Salalah, yet they charge you 
					$2,000 a night for a villa. So tourism has immense potential 
					in Oman. <br>
					<br>
					I have come here with a lot of energy, hope and optimism. 
					What a lot of people do not realise about Muriya is the 
					difference between us and what else is on offer. People 
					consider us to be a real estate company just like some other 
					developers. I beg to differ with this – we take barren land 
					and generate value around it, by creating an integrated 
					city. What people do not realise is that a property 
					developer comes in where there is infrastructure, proven 
					demand and he puts a few buildings and either sells them or 
					rents them. A property developer will not take a place where 
					there is no infrastructure and create livelihood around it, 
					but a company like Muriya takes land where there is no 
					habitation and by definition this has to be out of town and 
					in the middle of nowhere. We look at the natural beauty of 
					the place and leverage it to create something out of it. 
					This is therefore a very long term project. <br>
					<br>
					When your investment horizon is so long you tend to be 
					conservative and plan for downturns and upswings. So we take 
					land out of nowhere and create a tourism destination. We 
					have no end game, when we develop the project guess who is 
					going to be the biggest property owner – it will be Muriya. 
					As we will continue to own a significant percentage of most 
					of the hotels, we will continue to hold large scale real 
					estate inventory that we will continue to develop. Our 
					interest is that the value of the destination is valued and 
					kept. The message is that we are not experimenting with 
					customer money. We are not here because sales is good or the 
					market is hot – this is our business, this is what we do and 
					we know nothing else. Since the crisis no foreign investor 
					has put so much money in the GCC as Muriya has åthrough its 
					shareholders into Oman. <br>
					<br>
					<br>
					<b>What are the board’s expectations and is there a future 
					roadmap?<br>
					</b><br>
					In this business there are three or four major pillars – 
					there is sales and marketing, construction, financing 
					operations which are using your equity more intelligently 
					and having as much sensible debt as possible and then there 
					is the community operation of creating livelihood and 
					customer experience. On the construction front we are cursed 
					as we are out of town. If we were in Ghubra, you could put 
					up a crane and the world would know about it, but If you are 
					in Sifah, not only are your transportation and insurance 
					costs higher but no one sees you either.<br>
					<br>
					A lot of work has happened in Sifah and Salalah. My plans 
					are to develop every one of those pillars. One area that we 
					could have done better is communication – letting people 
					know as to what we are doing and getting them to see what we 
					are doing. Though this may sound like rhetoric, I see every 
					challenge as an opportunity. We have a challenge in sales 
					and marketing so we need to position ourselves properly and 
					sell the concept well. We need to expand the sales channel, 
					the network and improve our distribution. When it comes to 
					financing we are not unique. We need to expand our financing 
					options as we have been solely equity dependent till now, 
					though we also have some customer payments. A number of 
					banks are interested in financing our hotel developments and 
					we are speaking to them. <br>
					<br>
					<b>There is a strong impression that things have slowed down 
					in all the ITCs including Muriya. How would you react to 
					this? </b><br>
					<br>
					I agree that we have a perception hurdle, in the last two 
					years or so the world was falling apart and real estate was 
					the biggest victim along with financial institutions. Real 
					estate in the region was created and defined in the way we 
					know it by Dubai. And as Dubai went through the floor, real 
					estate in the region went through a lot of problems. When 
					there is a huge tide coming and you stick your head out and 
					say we are different, there are going to be very few people 
					who are going to believe you. Even if we had said that it’s 
					business as usual in this scenario people would not have 
					believed us, so the wise thing was to lie low and to 
					continue doing your work. Now when people look around they 
					can see all the good work that has been done. <br>
					<br>
					<b>Are there any timelines on when the hotels, marina and 
					apartments at Jebel Sifah and Salalah Beach will be 
					operational or handed over? <br>
					</b><br>
					We will open the marina, the first hotel and handover the 
					first apartments in Jebel Sifah by the summer of 2011. The 
					landscaping around the apartments has been done; at the 
					Marina our target is to have 100 boats before the end of the 
					year and I am sure that we will get this as the competition 
					is very expensive. When we deliver these people will have a 
					reason to come. Today, if you want to go out of Muscat town 
					for a relaxing dinner your options are limited. We will have 
					10 hotels between the three projects, four of these will 
					open in the next two years, two in this calendar year and 
					two next year. The idea is to create traffic; we are talking 
					to prominent retail names in the region to come and take 
					space there and once that happens the marina will open and 
					apartment keys will be handed and it will be a mini 
					destination. </font></p>
