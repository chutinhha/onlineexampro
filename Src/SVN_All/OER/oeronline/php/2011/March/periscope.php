                  <p align="justify" style="margin: 3px 10px"> 
					<b><font face="Verdana" size="1" color="#C00000">&nbsp;</font><br>
					<font face="Verdana" size="4" color="#C00000">Egypt, risk 
					and Arab equities<br>
                    </font></b> 
					<font face="Verdana" size="1"><br>
					</font><b><font face="Verdana" size="2">The uprising in 
					Tahrir Square has triggered stock market crashes in Arab 
					bourses from Casablanca to Muscat<br>
					<br>
&nbsp;</font></b></p>
					<p align="center" style="margin: 3px 10px"> 
					<font face="Verdana" size="1"><br>
                    <img border="0" src="images/periscope-1.jpg" width="400" height="399" style="border: 1px solid #C0C0C0"><br>
                    &nbsp;</font></p>
					<p align="justify" style="margin: 3px 10px"> 
					<font face="Verdana" size="1">In the last three years, there have been some dramatic, if 
					not traumatic, real time lessons in risk for investors in 
					Arab equities. Egypt’s political crisis triggered stock 
					market falls in Saudi Arabia, UAE, Jordan and Qatar. After 
					all, many listed GCC companies have obvious exposure to the 
					Egyptian economy.<br>
					<br>
					DP World owns the Red Sea port of Sobkha, Air Arabia has an 
					Alexandria hub and flies to Cairo and Luxor, NBAD has 8 per 
					cent of its assets in its Egyptian branch network, Al Raghi 
					owns stakes in Sharia- compliant Egypt banks. This reflects 
					the rise in intra-Arab FDI and capital flows in the past 
					decade, a process that has also dramatically increased 
					correlation risk among different Arab stock exchanges. This 
					process has only been accelerated by the rise of a new breed 
					of pan Arab mutual fund and hedge fund manager, a financial 
					ecosystem that ensures that gunfire in Tahrir Square trigger 
					stock market crashes in Riyadh, Dubai, Amman and Doha. <br>
					<br>
					</font><font face="Verdana" size="2" color="#000080">
					<b>Collective risks</b></font><font face="Verdana" size="1"><br>
					<br>
					The region’s stock exchanges are also inter-connected by 
					powerful linkages due to cross shareholdings, strategic 
					ownership stakes, cross listings, GDR programmes and, above 
					all, margin accounts. Saudi private investors, for instance, 
					were forced to sell Saudi equities in Riyadh to meet margin 
					calls when Egypt’s stock market plunged from EGX – 30’s 
					7,200 January high to 5,600 before it was closed. Every 
					investor in any GCC stock exchange is exposed to the global 
					and Arab world risks which also includes geopolitical risks.<br>
					<br>
					Sovereign credit risk is also the fallout of the Egyptian 
					crisis. Egypt’s five-year sovereign credit default swaps 
					more than doubled to 400 basis points after street protests 
					broke out against the Mubarak government in late January. 
					This meant that the capital markets considered investing in 
					Egypt sovereign riskier than investing in Iraq! Credit 
					default swaps (CDS) are the crisis flash points of the 21st 
					century Arab finance. This widening of Egyptian sovereign 
					spreads will have traumatic real world consequences even as 
					the CDS market, notoriously illiquid and volatile, triggers 
					financial contagion across all Arab, even emerging market 
					issuers.<br>
					<br>
					Egypt has been twice downgraded by Moody’s. The fall in the 
					Egyptian pound will mean a spike in imported inflation, 
					particularly since Egypt is the world’s largest wheat 
					importer at a time when Russian drought led to ban on grain 
					shipments. While bread has been subsidised in Egypt since 
					the Pharonic times, food riots could well erupt if crude oil 
					and grain prices continue to spiral higher across the 
					emerging markets.<br>
					<br>
					The rise in Egypt’s CDS means higher borrowing costs for a 
					state with 8 per cent budget deficit and $45bn in maturing 
					local currency debt that cannot be rolled over to foreign 
					investors. A depositor run on Egyptian banks when they 
					reopen could lead to another spiral higher in Egypt’s 
					borrowing costs, country risk premium and CDS. This will 
					mean refinancing risk will suddenly become a crucible of 
					contagion for all the Arab borrowers. This, in turn, will 
					pressure Arab bourses from Casablanca to Muscat and, ipso 
					facto, lead to a rise in equities risk premia across the 
					region. This is the nature of New Age international finance. 
					It escalates local crises into regional shocks at the speed 
					of life. Nakheel’s shock standstill request to its creditors 
					in December 2009 triggered a regional surge in risk premia.
					<br>
					<br>
					Banking counterparty risk has shot higher in the region in 
					the aftermath of Dubai’s sovereign debt restructurings, 
					several Kuwait sukuk defaults, the collapse of two Bahraini 
					offshore banks owned by Saudi Arabia’s Saad/Gosaibi Groups 
					embroiled in a debt scandal and, above all, the failure of 
					Wall Street investment bank Lehman Brothers. This is 
					relevant to Arab stock exchanges for two reasons. One, loan 
					growth in several GCC banking systems in nowhere near 
					matched by organic deposit growth. This is the reason 
					loan-deposit ratios in several GCC banks were well above 100 
					per cent, making them highly leveraged and dependent on 
					raising wholesale funds in the offshore medium term note 
					Euromarkets. These wholesale markets are now virtually 
					closed for Arab banks who hail from heavily indebted states 
					or governments gripped by fiscal and political crises. Two, 
					Arab stock exchanges are dominated by banks, insurers and 
					property companies.<br>
					<br>
					Any impact on bank risk has immediate, negative impact on 
					Arab stock exchanges. This huge weighting of banks in 
					regional exchanges is the reason why GCC markets, burdened 
					by the Nakheel/DP World, Saad/Gosaibi and the Kuwaiti sukuk 
					defaults, grossly underperformed BRIC’s and Asian emerging 
					markets in 2009 and 2010. Investors in Arab equities should 
					remember that Egypt’s Commercial Bank and International and 
					National Bank of Egypt alone are more than one third of the 
					free float in the Cairo Alexandria stock exchanges. Any 
					increase in Egyptian bank risk will therefore echo across 
					the Arab world. An increase in borrowing costs in the 
					offshore funding market is inevitable for Arab bank 
					borrowers, even those from countries with no systemic debt, 
					fiscal or political crises.<br>
					<br>
					Egypt also reinforces the role of liquidity risk in Arab 
					stock exchanges. Foreign funds own one third of the largest, 
					most liquid Egyptian telecom, banking, property, steel and 
					construction shares. In times of political crises, Western 
					funds heed the credo of Victorian surgeons. When in doubt, 
					cut it out. They sell what they can, not what <br>
					they must.</font></p>
