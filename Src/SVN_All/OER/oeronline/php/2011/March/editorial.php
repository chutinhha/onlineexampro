				</b><font face="Verdana" size="1"><br>
                Real estate is real while everything else is unreal’ is an adage 
				that one often hears in Oman. The swagger and hubris of the 
				sector took a beating in the aftermath of the subprime crisis as 
				loan defaults, project delays, eroding capital values, declining 
				rentals, liquidity issues and lack of customers reared their 
				head. To be fair, Oman fared much better than its peers in the 
				region and things have stabilised to a large extent – 
				speculators have given way to real buyers and sellers, there is 
				a better demand and supply match in the residential space and 
				banks have started lending to developers and customers.<br>
				<br>
				On the flip side, things remain challenging in the office space 
				market. According to a CB Richard Ellis report, “Despite 
				improving conditions economically, the office market continues 
				to face major challenges as significant oversupply impacts the 
				market for both existing as well as new office buildings. Tenant 
				migration trends are set to be maintained during 2011, as 
				occupiers look to move from aging buildings to new dedicated 
				office developments with higher quality specifications and 
				adequate facilities. In the short term, excess supply will 
				supersede demand resulting in improved terms for tenants in the 
				form of rent free periods and more relaxed payment terms.” 
				Integrated Tourism Complexes are similarly being forced to 
				rethink their strategies as European buyers have decreased due 
				to economic compulsions.<br>
				<br>
				Despite such shortcomings,no one can deny the primacy and 
				importance of real estate for Oman’s economy. The government’s 
				thrust on infrastructure spending and economic growth are 
				expected to spur the sector in the years ahead. OER’s cover 
				story takes stock of various cross currents impacting realty in 
				the Sultanate.<br>
				<br>
				The sudden and untimely demise of HE Dr Rajiha bint Abdulamir 
				bin Ali, Minister of Tourism came as a huge shock to all of us. 
				As a tribute to HE Dr Rajiha, OER spoke to her husband Murtadha 
				Hassan Ali and her son Faisal Murtadha Al Lawati. The article 
				makes for poignant reading.<br>
