						<p align="justify" style="margin: 10px; ">
				<font face="Verdana" size="1">The 2011 Jeep Grand Cherokee can 
				conquer nearly anything off the beaten path. It is available 
				with a choice of three four-wheel drive systems. Quadra-Trac I 
				is a full-time system with a single-speed transfer case; it 
				doesn’t require any input from the driver. Quadra-Trac II 
				provides a two-speed transfer case, and Quadra-Drive II features 
				a full-time transfer case with a rear Electronic Limited-Slip 
				Differential. The system detects tire slip and distributes 
				engine torque to tires with the most traction. The Grand 
				Cherokee provides up to 8.6 inches of ground clearance with the 
				standard suspension and up to 10.7 inches with the optional air 
				suspension. New for this year, the Quadra-Lift air suspension 
				system is standard on Overland models and optional on Laredo X 
				and Limited models with 4WD.<br>
				<br>
				The Quadra-Lift air suspension system can be operated 
				automatically or manually and features five different height 
				settings. Normal Ride Height provides 8.1 inches of clearance; 
				Off-road 1 lifts the SUV an extra 1.3 inches to help clear 
				obstacles; Off-road 2 provides an extra 2.6 inches; Park Mode 
				lowers the vehicle 1.5 inches to make it easier to enter and 
				exit; and Aero Mode lowers the vehicle half an inch to make the 
				Grand Cherokee more aerodynamic and provide maximum fuel 
				economy.<br>
				<br>
				The air suspension system makes the Grand Cherokee comfortable 
				both on and off the pavement. The Selec-Terrain system, which is 
				standard on models with Quadra-Trac II and Quadra-Drive II 
				offers modes for five types of driving conditions: Sand/Mud, 
				Sport, Auto, Snow and Rock. If you are looking for an off roader 
				which can double up as a comfortable city drive, then your 
				search ends with the Jeep Grand Cherokee.</font></td>
