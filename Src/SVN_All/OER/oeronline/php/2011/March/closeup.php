                  <p align="justify" style="margin: 3px 10px"> 
					<b><font face="Verdana" size="4" color="#C00000"><br>
                    GCC’s drive for clinching trade deals<br>
                    </font><font face="Verdana" color="#C00000" size="2">&nbsp;</font><font face="Verdana" size="4" color="#C00000"><br>
                    </font></b><font face="Verdana" size="2"><b>The GCC 
					countries are bound to expand business opportunities in the 
					region by clinching trade deal with countries and nation 
					groups across the world</b></font></p>
                  <p align="justify" style="margin: 3px 10px"> 
					&nbsp;</p>
                  <p align="justify" style="margin: 3px 10px"> 
					<font face="Verdana" size="1">
                    The Gulf Cooperation Council (GCC) countries are in a mood 
					to conclude trade deals with various countries and economic 
					entities across the globe. Undoubtedly, this drive fits well 
					with the tenor of globalisation where nations depend on each 
					other for their economic well-being.<br>
					<br>
					The latest move was the signing of a framework agreement on 
					economic, commercial, investment and technical cooperation 
					with Malaysia. The high profile deal was signed in Abu Dhabi 
					earlier in 2011, reflecting UAE’s presidency of the GCC 
					until the next summit in Saudi Arabia.<br>
					<br>
                    <br>
					</font> 
                    <b> 
					<font face="Verdana" size="2" color="#000080">
                    Mutual benefits 
					</font> 
					<font face="Verdana" size="1">
					<br>
					<br>
					</font> 
                    </b> 
					<font face="Verdana" size="1">
                    The deal seeks to further strengthen the bilateral trade 
					between GCC states and Malaysia by making it easier for 
					investors to do business in the countries on each side. 
					Available statistics put the deal at more than $30bn. 
					Understandably, Malaysia enjoys a $1.5bn surplus on the back 
					of exporting their relatively pricey items which includes 
					durable goods such as equipments and machineries. 
					Conversely, GCC exports primarily comprise of crude oil, 
					petroleum products and petrochemical goods, whose prices are 
					vulnerable to market prices.<br>
					<br>
					Undoubtedly, Malaysia is bound to benefit enormously from 
					emerging business opportunities in the region on the back of 
					gradual return of confidence in the aftermath of the global 
					financial crisis. The steady oil prices in the international 
					markets testify to this. Needless to say, the petroleum 
					sector serves as the main source of budgetary revenues and 
					hence the expenditures in all GCC countries.<br>
					<br>
					To be sure, Malaysian firms are key investors in Doha’s new 
					airport. What’s more, Malaysia stands the chance of winning 
					projects related to World Cup 2022 in Qatar thanks in part 
					to trade accords with GCC countries at large.<br>
					<br>
					For its part, Malaysia aims at attracting GCC direct 
					investments for property development throughout the country. 
					In addition, the deal stands to further strengthen the 
					attraction of Malaysia as a tourist destination for GCC 
					citizens. Numerous GCC-based carriers operate daily flights 
					to Kuala Lumpur.<br>
					<br>
					</font> 
					<br>
                    <b><font face="Verdana" size="2" color="#000080">Looking eastward</font><font face="Verdana" size="1"><br>
					</font>
					</b>
					<font face="Verdana" size="1">
					<br>
                    It is hoped that the latest accord would pave the way for 
					clinching a free trade agreement (FTA) with Malaysia in a 
					span of two years. By one account, negotiations between 
					representatives of the two sides are scheduled to start in 
					March.<br>
					<br>
					It makes sense for Malaysia to have an FTA with a group of 
					six nations of sizable economies. According to a World Bank 
					rating in 2010, the gross domestic product (GDP) of Saudi 
					Arabia is ranked number 20 worldwide. By contrast, GDP of 
					Malaysia ranks number 29 globally, smaller than that of 
					Saudi Arabia alone. Together, the GDP size of the GCC 
					countries combined is twice that of Malaysia.<br>
					<br>
					The GCC’s drive for signing free trade agreements (FTA) and 
					other framework accords dates back to 2008 when it signed 
					the first ever FTA with Malaysia’s neighbour Singapore. It 
					ushered in a new period of trade deals with the members of 
					the Association of South East Asian Nations (ASEAN). 
					Concluding a deal with ASEAN as a bloc is a daunting task 
					because of the uneven economic situations in the 10-member 
					grouping. Asean includes countries facing different economic 
					realities including Singapore and Malaysia on the one hand 
					and Cambodia Laos on the other.<br>
&nbsp;</font></p>
					<p align="justify" style="margin: 3px 10px"> 
					&nbsp;</p>
					<p align="justify" style="margin: 3px 10px"> 
                    <b><font face="Verdana" size="2" color="#000080">EFTA and New Zealand</font></b><font face="Verdana" size="1"><br>
					<br>
					In 2009, GCC countries together signed another FTA with the 
					European Free Trade Association (EFTA), which comprises of 
					Switzerland, Norway, Iceland and Liechtenstein. Amongst 
					others, the deal suggests that several European countries 
					such as Switzerland and Norway seem pleased with the laws 
					and procedures in place in the GCC. Yet, the biggest 
					challenge for GCC countries relates to reaching FTA with 
					27-nation European Union, the largest economic bloc 
					worldwide. Undoubtedly, this is not an easy job partly 
					because of EU’s numerous demands especially in matters 
					related to human rights and environmental protection in the 
					GCC countries.<br>
					<br>
					On the other hand, the GCC states insist on removal of 
					customs charges on aluminum and petrochemicals products. 
					Currently, the EU imposes notable customs duties on imports 
					of aluminum and petrochemical products from the GCC nations 
					in return for supplying industrial undertakings with 
					under-priced gas.<br>
					<br>
					Also in 2009, GCC states and New Zealand came close to 
					reaching a comprehensive FTA. Not surprisingly, New Zealand 
					with its unique geographical location, showed particular 
					interest in concluding the deal. It was in 2006 that the two 
					sides agreed to start work towards reaching a deal. 
					Negotiations kicked off in 2007 only to be completed in late 
					2009 following merely six rounds of talks. Besides, GCC 
					carries deserve credit for operating flights to New Zealand, 
					thereby paving the way for strengthening business links 
					between the two sides.<br>
					<br>
					The GCC states also signed a memorandum of understanding in 
					the first half of 2010 on commercial and economic 
					cooperation with 19-member Common Market for Eastern and 
					Southern Africa (COMESA) focusing <br>
					on economic cooperation.<br>
&nbsp;</font></p>
					<p align="justify" style="margin: 3px 10px"> 
					<b><font face="Verdana" size="2" color="#000080">The way forward</font><font face="Verdana" size="1"><br>
					</font>
					</b> 
					<font face="Verdana" size="1"><br>
					There are indications that the GCC states are determined to 
					sign trade deals with other countries, with talks underway 
					with Japan, South Korea, Pakistan, Australia and China. The 
					Chinese seem to be interested in restricting any such deal 
					in the first place to goods, thereby excluding services. 
					Undoubtedly, China is known for enjoying competitive 
					advantage in exporting goods but not services. But a deal 
					with the world’s second largest economy has a special 
					significance for GCC countries whilst pursuing economic 
					agreements with other nations.<br>
					<br>
					GCC states are doing the right thing by clinching trade 
					accords with entities and countries located in diverse parts 
					of the world. Regional countries would most likely not be at 
					loss by making trade deals by virtue of being relatively 
					open economies. In fact, numerous entities and countries 
					enjoy trade surpluses with GCC countries outside the 
					petroleum industry.</font></p>
