				<font face="Verdana" size="2"><b>The first ever event in Oman 
				dedicated to the construction sector, Oman Construction Summit 
				2011 underscored the strong interest in the Sultanate as the 
				government embarks on massive infrastructure development 
				throughout the country</b></font><b><font face="Verdana" size="4" color="#C00000"><br>
&nbsp;</font></b><p align="center" style="margin: 3px 10px" dir="ltr">
				<img border="0" src="images/inthenews-1.jpg" width="450" height="289"><p align="justify" style="margin: 3px 10px" dir="ltr">
				<font face="Verdana" size="1"><br>
				<br>
                Oman Construction Summit 2011 that concluded in Muscat recently 
				highlighted the rapid progress being made by the Sultanate in 
				infrastructure development and investment opportunities in the 
				construction sector.<br>
				<br>
				Organised jointly by Global Exhibitions and Conferences (GEC) 
				and the US-based International Quality and Productivity Centre (IQPC) 
				under the patronage of the ministry of housing, the summit 
				provided exclusive details of the progress in some of the key 
				projects like Duqm port, dry-dock, and Oman national railway 
				project.<br>
				<br>
				Over 40 high profile speakers which included top officials from 
				the government and the private sector made presentations at the 
				summit, which was the first ever event in Oman dedicated to the 
				construction sector. The panel discussions were marked by candid 
				reviews of development strategies. The event was formally 
				inaugurated by HH Sayyid Harib bin Thuwaini al Said, assistant 
				secretary-general of the council of ministers for conferences, 
				in the presence of ministry officials, members of the diplomatic 
				community, the senior management of construction companies and 
				other distinguished guests.<br>
				<br>
				</font><font face="Verdana" size="2" color="#000080"><b>Focus on 
				Duqm</b></font><font face="Verdana" size="1"><br>
				<br>
				Dr Maurice Girgis, project advisor, who made a presentation on 
				the focus day, said Duqm Port phase one would be ready by 2012. 
				Director general of ports at the ministry of transport and 
				communications, Qassim Ahmed al Shizawai, revealed that RO1.6bn 
				has already been invested on Duqm’s infrastructure development. 
				Infrastructure development is progressing on a war footing and 
				investors can avail of the advantages offered by Duqm special 
				economic zone to gain competitive edge.<br>
				<br>
				M J Park, CEO, Oman Drydock Company, says the soft launch of the 
				dry-dock will be held in April. Explaining the facilities at the 
				dock, he points out that Duqm dry-dock was the second biggest in 
				the region and has the advantage of being brand new, compared to 
				Dubai dry-dock.<br>
				On the progress of Oman’s railway project, Engineer Salim Al 
				Afani, director general for physical planning, supreme committee 
				for town planning said that pre-qualification process for 
				project management and design and supervision consultants has 
				been completed and consultants will be selected by June this 
				year. “Railways will start operations in Oman in 2017,” he adds.<br>
				<br>
				Arjan Weerstand, acting general manager, port development and 
				implementation, Port of Salalah revealed the expansion of 
				general cargo terminal is going on and expansion of container 
				terminal will be taken up in the fourth phase which will be 
				completed in 2019. The summit also featured presentations on 
				Oman’s tourism infrastructure, key residential projects, the 
				ministry of housing’s strategic vision, the expansion of Salalah 
				Port, and Oman’s national solid waste management strategy. <br>
				<br>
				Speakers also discussed the country’s water infrastructure 
				projects, the competitiveness and attractiveness of Oman’s 
				construction market, the key challenges facing the Sultanate’s 
				construction industry, the future plans of Petroleum Development 
				Oman (PDO), and how to manage risks in construction projects.<br>
				<br>
				Case studies were also presented on the Asian beach games 
				project, the Muscat wastewater project, and The Wave, Muscat. In 
				addition, there were panel discussions on the construction 
				market forecast for the next five to ten years as well as on the 
				challenges and best practices in developing Oman’s energy and 
				utilities infrastructure.</font><p align="right" style="margin: 3px 10px"> 
