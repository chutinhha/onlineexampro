						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">Attention to detail</font></b><p align="justify" style="margin: 3px 10px">
				<b><font face="Verdana" style="font-size: 9pt"><br>
				<img border="0" src="images/nov-industryreport1.jpg" style="border: 1px solid #808080" class="style4" hspace="8" vspace="4" width="100" height="114" align="right">IndiGo Airline started service on 
				the Muscat-Mumbai sector from 10 October 2011. Aditya Ghosh, 
				President, IndiGo speaks to OER about the airline’s 
				differentiation</font></b><p align="justify" style="margin: 3px 10px">
				<font face="Verdana" size="1"><br>
				<b>Over the last five years, IndiGo has captured a 20 per cent 
				marketshare in India. What accounts for your success? <br>
				</b>It’s the consistency of the product. IndiGo concentrates on 
				three simple things: We keep our planes flying on time and our 
				on-time performance is the best in the country. We keep the 
				whole experience hassle-free and there is a lot of courteousness 
				in our service. Finally, we consistently have low fares. We are 
				a young airline, but we are the fastest growing airline in India 
				and the fastest growing low-cost airline in the world.<br>
				<br>
				<b>IndiGo’s has an on-time performance of over 90 per cent 
				domestically and close to 95 per cent internationally. How do 
				you maintain these standards?<br>
				</b>Different airlines decide to concentrate on different things, we 
				feel that our on-time performance is something that is very dear 
				to us and that’s what we focus on. Our entire crew and staff is 
				focussed on keeping our operations safe and on time.<br>
				<b><br>
				Though IndiGo flies to 26 destinations, your load factor is also 
				quite impressive, is this largely driven by low pricing?<br>
				</b>It is not just pricing because pricing can be easily matched. 
				One big element is keeping the fares consistently low but the 
				other big element is how easy and hassle free the whole 
				experience is. Air travel can be quite an intimidating 
				experience for first time or infrequent flyers. So we constantly 
				work on providing a pleasant experience. We were the first 
				airline to introduce queue busters, or hand held devices which 
				kept our check in lines short. We use boarding ramps as a number 
				of airports do not have aero bridges. This makes it easier for 
				families with kids, strolley bags and wheel chair passengers to 
				board the aircraft.<br>
				<br>
				<b>The airline has a staff strength of 4,200 people and plans to 
				recruit another 1,000 people soon – this makes you one of the 
				fastest growing employers in the market. How do you ensure that 
				they are competent and efficient?<br>
				</b><br>
				We create 100 job opportunities every month and stress a lot on 
				training. Finding the right people in an industry like ours is a 
				big challenge and if we are going to create an airline for the 
				future then we need to equip our people with the right skills. 
				We have a central pool called I-Fly unlike other airlines we 
				train our staff in a number of disciplines at I-Fly. The academy 
				has pilots, flight attendants, engineers and airport operations 
				staff coming to train. They not only get trained in functional 
				skills but also in customer service, leadership, communication 
				skills. We have two separate programme running for pilots. 
				Firstly, we hire young pilots who hold a Commercial Pilot 
				License (CPA). And then they go through a test conducted by CAE 
				on our behalf. This is followed by an interview process which is 
				a very rigorous screening. If they qualify in this test then we 
				send them on our own expense to get a type rating done and fly 
				the A320 and join us as junior co-pilots. The second programme 
				we have is for young graduates who do not have any pilot 
				training but are keen on becoming pilots. So they write an exam 
				and then they qualify and then the National Flights Training 
				Institute trains them in grading a CPL. Then an international 
				facility will help them to get an A320 type rating; and we give 
				them a certain stipend when they are undergoing this training. 
				We have a similar programme for engineers. <br>
				<br>
				At IndiGo it’s all about chasing dreams whether it is our 
				customers, employees or shareholders.<br>
