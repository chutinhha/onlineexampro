						innovative mechanical supercharging system with charge 
						air cooler is employed, and turns the 3.0 TFSI V6 into a 
						compact powerhouse. The peak torque of 420 Nm also 
						leaves one impressed. And all this power and torque is 
						achieved with a lower fuel consumption thanks to Audi’s 
						efficient technologies such as the recuperation system 
						and the innovative thermal management.<br>
						<br>
						The transmission also plays a major role in the 
						efficiency of the A8 L. The eight-speed tiptronic has a 
						very high overall gear ratio and is controlled 
						electronically using either the elegant selector lever 
						or paddles on the steering wheel. The quattro permanent 
						all-wheel drive has a distinctly sporty and rear-biased 
						character. The optional sport differential dynamically 
						distributes the forces between the rear wheels; this 
						feature is standard with the A8 L 4.2 TDI.<br>
						<br>
						The new A8 L offers both luxurious ride comfort and 
						sporty handling. Its wheel control arms are made of 
						aluminium. The adaptive air suspension with controlled 
						damping is standard equipment and is integrated into the 
						versatile Audi drive select dynamics system. The 
						optional dynamic steering completes the range of chassis 
						options.</font></td>
