						<font face="Verdana" size="1" class="style11" style="font-size: 11px">
						The Audi A8 L impresses with a presence of powerful 
						confidence – it is a large, distinguished luxury sedan. 
						It’s length and wheelbase have both been stretched by 13 
						cms (5.12 in) versus the standard version; the A8 L 
						measures 5.27 mtrs (17.29 ft). All of this added length 
						is to the benefit of the rear seat passengers. The 4.2 
						litre V8 engine in the Audi A8 uses the famed FSI 
						technology.<br>
						<br>
						Audi offers two power-adjustable and heated individual 
						seats as an option for the rear, which can be paired 
						with the optional continuous, elegantly-styled centre 
						console with large storage compartments. The individual 
						seats can also be equipped with optional ventilation and 
						massage functions, and features such as a folding table 
						and a cool box make the passengers’ stay even more 
						enjoyable. The top version is the relaxation seat with 
						power footrest. It comes complete with every available 
						luxury option, including a rear seat entertainment 
						system. <br>
						<br>
						It also offers a number of attractive multimedia 
						modules, including a high-end sound system from Bang &amp; 
						Olufsen and advanced online services. The Audi A8 L is 
						the first automobile in the world to be available with 
						an integrated WLAN hotspot. A UMTS module provides the 
						connection to the internet.<br>
						<br>
						The interior of the A8 L enchants with its new, light 
						lines, with a craftsman’s level of fit and finish and 
						with extraordinary attention to the smallest of details. 
						The comfort on board is first class – from the ambient 
						lighting to the four-zone automatic climate control 
						system.<br>
						The Audi flagship combines a new level of technical 
						intelligence with intuitively simple operation. Its 
						multimedia interface (MMI) includes a pioneering 
						innovation: In conjunction with the optional MMI 
						navigation plus navigation system, it has a touchpad. 
						The MMI enables the driver to control many functions 
						intuitively. The navigation destination, for example, 
						can be entered by tracing the letters on the pad with a 
						finger.<br>
						<br>
						MMI navigation plus is tightly integrated with the 
						assistance and safety systems in the Audi A8 L. It reads 
						the road ahead and provides the data to the control 
						units for the automatic transmission, the headlights and 
						the advanced adaptive cruise control with stop and go 
						function.<br>
						<br>
						With their increased intelligence, these systems can 
						recognise complex scenarios and predictively support the 
						driver. The new Audi pre sense safety system can reduce 
						the severity of accidents and their consequences in many 
						cases. The night vision assistant with its highlighting 
						of detected pedestrians and the speed limit display 
						enhance the driver’s safety and convenience.<br>
						<br>
						<b>Timeless elegance<br>
						</b>The Audi A8 L is a sedan of classic, timeless 
						elegance. Its body is an aluminium Audi Space Frame (ASF), 
						weighing about 40 per cent less than a comparable steel 
						structure. Audi offers optional LED headlights that use 
						light emitting diodes for all light functions (standard 
						with the W12). These represent a revolution in lighting 
						technology with respect to both the technology and 
						appearance.</font><p align="justify" style="margin: 3px 10px">
