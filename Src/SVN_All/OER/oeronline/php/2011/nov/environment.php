				<b><font size="2" face="Verdana">Over the last 25 years, Al 
				Bustan Palace Hotel has become a national landmark complementing 
				Oman’s rich natural heritage. Known as the ‘Jewel of the 
				Sultanate’, the hotel is committed to environmental preservation 
				by undertaking various initiatives related to energy management, 
				water management, waste management and environmental awareness.<br>
				</font></b><font size="1" face="Verdana"><br>
				Al Bustan Palace sewage water treatment plant is one of the most 
				environmentally friendly and economically sustainable 
				initiatives undertaken by the hotel. Located adjacent to the 
				palace, the facility has the capacity to manage all the waste 
				water from the hotel and Al Bustan village, a charming fishing 
				village located in close proximity to the hotel with which the 
				palace has a deep and longstanding relationship. <br>
				<br>
				The waste water is re-cycled, thus eradicating negative 
				ecological impacts to the local marine life and minimising water 
				requirements, which effectively results in cost reductions and 
				decreased CO2 emissions. The brown water that leaves the hotel 
				is turned into irrigation water to be used in the local 
				agricultural fields and the hotel landscape.<br>
				<br>
				With annual rainfall in Muscat averaging just 10 cm or 4 inches 
				per annum, maintaining an adequate supply of water for 
				agricultural and domestic use is a pressing need which Al Bustan 
				Palace decided to tackle head on. The sewage water treatment 
				plant enables the hotel to recycle 780 CBM per day. The cost of 
				that water would be RO429. If the water had to go through a 
				desalination plant, it would take 0.86 kWh of energy to 
				desalinate 1 m3 of salt water.<br>
				<br>
				<b>Optimising natural daylight<br>
				</b><br>
				The hotel also uses an automated lighting system, which 
				calculates sunrise and sunset and even takes into account the 
				season of the year, in order to save lighting consumption and 
				optimise natural daylight.<br>
				<br>
				The hotel uses 11W CFLs (Compact Fluorescent Light bulbs) to 
				save energy, and rooms are equipped with Thermostat control 
				feature. During the time of lower occupancy, room allocation is 
				optimised by concentrating guests in certain areas of the 
				building; this practice enables the hotel to seal off other 
				areas as well as reducing energy consumptions in the 
				non-occupied areas. In addition, the hotel has recently adopted 
				global principles of Geo-Tourism to enrich the understanding of 
				environmental and cultural responsibility.<br>
				<br>
				For meetings and events, the hotel uses cloth napkins and 
				provides glasses – rather than Styrofoam or plastic – and does 
				not use plastic coaster and foam cups. It also promotes local 
				food through Seblat Al Bustan where guests can enjoy an 
				enriching and locally inspired experience.<br>
