				<font size="1" face="Verdana"><br>
				The Best prize that life offers is the chance to work hard at 
				work worth doing,” remarked Theodore Roosevelt, the American 
				President in a Labour Day speech on September 7, 1903. In the 
				wake of the global recession, this prize seems to be more 
				elusive than ever. Worldwide 250mn people are currently 
				unemployed. In some countries long term unemployment has been 
				growing steadily both in absolute terms and as a proportion of 
				the total joblessness. In the US this figure stands at 30 per 
				cent, up from 10 per cent in 2007. The other side of the coin – 
				more than 3.1bn people will be employed across the globe in 
				2011, more than ever before in human history. Despite this there 
				is a sense of uncertainty and gloom about jobs and employment in 
				developed economies. <br>
				<br>
				Creating jobs and making young people readied for jobs is one of 
				the biggest challenges facing government’s across geographies. 
				Closer home, what can be done to make Omanis better prepared for 
				a variety of jobs? Well, skilling them would be a good start. In 
				economic terms a person can use human capital, social capital or 
				economic capital to succeed in life. Human capital translates 
				into education, training and learning. Social capital comprises 
				one’s circle of friends, contacts and acquaintances and economic 
				capital is wealth or money that one has at his or her disposal. 
				Looking at the picture it is clear that social and economic 
				capital is largely a function of human capital – the primary 
				resource. <br>
				<br>
				Upgrading Oman’s human capital is an issue that requires the 
				participation of government, companies and the youth. A lot of 
				initiatives are being taken by the government in this direction. 
				It’s time now for the private sector to stand up and contribute 
				to this goal. Losing sight of this national priority would not 
				just be short-sighted but suicidal.<br>
