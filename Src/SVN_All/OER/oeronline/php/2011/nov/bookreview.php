						<p align="justify" style="margin: 10px">
				<font face="Verdana" style="font-size: 8pt">
				<img border="0" src="images/nov-bookreview.jpg" width="200" height="287" style="border: 1px solid #808080" align="right" hspace="8"></font><font face="Verdana" style="font-size: 9pt; font-weight: 700">Leading 
				Smart Transformation asserts that governmental effectiveness and 
				economic prosperity are inextricably linked, hence the 
				importance of redesigning government to achieve effective change</font><p align="justify" style="margin: 10px">
				<font face="Verdana" size="1">Harry Truman has famously quipped, 
				“Wherever you have an efficient government you have a 
				dictatorship.” When unbridled capitalism and commerce were 
				touted to be the most successful and effective force for social 
				change, there was an attempt to downplay the role of governments 
				in safeguarding the future stability and prosperity of their 
				people.<br>
				<br>
				An important reason cited to justify this argument has been that 
				most of the governments are ponderous, conservative and 
				slow-moving behemoths.<br>
				<br>
				While corporations have proved adept at change, governments have 
				continued to govern according to established rules. While many 
				in government exhort captains of industry to embrace 
				innovations, they have not done so in their own sector.<br>
				<br>
				However, the recent governmental intervention to avoid an 
				economic meltdown from the banking crisis has reinforced the 
				point that governments have an important, sometimes, vital 
				economic role. In an increasingly globalised and connected 
				world, governments will have an effective role in creating, 
				supporting and nurturing the future competitive position of 
				cities, regions and nations.<br>
				<br>
				The book under review, Leading Smart Transformation - A Roadmap 
				for World-Class Government, talks about the urgent need to 
				overhaul the machinery of government to make the public sector 
				work more effi</font><font face="Verdana" size="1">ciently and 
				become a true enabler of economic and social progress. <br>
				<br>
				Jointly written by four distinguished scholars-Andrew Kakabasde, 
				Mohamed Omar Abdullah, Rabin Abouchakra and Ali Qassim Jawad-the 
				book offers crucial insights into the strategies of the most 
				effective governments in the world, drawing on decades of 
				research and experience. <br>
				<br>
				At the heart of the book, and the concept of smart 
				transformation, is the idea that government along with 
				corporations must take responsibility for finding a balanced 
				business model; one that meets the needs of shareholders as well 
				as other stakeholders, such as employees, customers and local 
				communities. <br>
				<br>
				Leading Smart Transformation describes a holistic, rapid, 
				flexible, dynamic approach, where leadership is integral to the 
				process, stakeholders are fully engaged, and last but not least, 
				the transformation programme is well planned with a clear 
				roadmap linking the various components of the cycle.<br>
				<br>
				The book examines the catalytic power of leadership in creating 
				lasting change. The successful transformation of governments, a 
				more sizable and troubling challenge, requires a competent 
				leadership. <br>
				<br>
				The smart transformation model allows the governments to 
				translate the leadership vision into a workable and measurable 
				strategic plan. It ensures that the necessary human talent, 
				organizational design and execution focus is in place to make 
				the vision a reality. <br>
				<br>
				The book also looks into certain important areas which are 
				essential to drive home the smart transformation such as 
				adopting global best practices, X-factor leadership, discipline 
				of change, engaging stakeholders etc. <br>
				<br>
				There is no denying the fact that governmental effectiveness and 
				economic prosperity are inextricably linked. At a time when 
				global geopolitics and world economy have undergone unparalleled 
				change and uncertainty, the need for smart government could not 
				be starker. <br>
