						<p align="justify" style="margin: 3px 10px">
						<font face="Verdana" size="4" color="#C00000"><b>What 
						next in energy and metal market?<br>
						<br>
						</b></font><font face="Verdana" style="font-size: 9pt">
						<b>Investors should bottom fish for oil/gas shares only 
						when Brent trades at the lower end of its $90-110 range<br>
						</b></font><font face="Verdana" size="1"><br>
						<i><font color="#333333">BY Matein Khalid<br>
						</font></i><br>
						Oil prices have softened since last summer due to the 
						geopolitical risk premium that began when Benghazi 
						revolted against the Gaddafi regime and NATO began its 
						bombing campaign. Even though Libya’s 1.6 million 
						barrels a day oil production has dwindled to a mere 
						100,000 barrels, the formation of a legitimate 
						government in Tripoli has assuaged market fears about a 
						protracted supply shock of light sweet Libyan crude.<br>
						<br>
						However, three major macro forces suggest oil prices 
						could move as low as $90 Brent and $70 West Texas in the 
						months ahead. One, Saudi Arabia is producing 9.8 million 
						barrels a day, 1.8 million barrels above its OPEC quota. 
						This means the OPEC quota and pricing regime basically 
						ended last June in Vienna as the kingdom, with its 
						massive spare capacity, acts as the central banker and 
						swing producer of OPEC. The Saudis have not indicated an 
						output cut is imminent. Two, global gasoline demand has 
						clearly begun to decline, as economic growth in the US, 
						Europe and China loses momentum. Three, the Eurozone 
						debt crisis and extreme volatility on Wall Street has 
						led to a major bullish breakout in the US Dollar Index. 
						As OPEC production hits 30 million barrels a day while 
						demand softens, $90 Brent is entirely realistic.<br>
						<br>
						I doubt if oil collapses as long as the US and Europe 
						does not fall into double dip recession. North Sea 
						inventories are still low as Caspian Sea and Nigerian 
						supplies are constrained. Moreover, geopolitics in the 
						Arab world, Nigeria and Venezuela will prevent a 
						collapse in the prices. China and India are still 
						growing at 7-9 per cent GDP growth rates. These trends 
						all reinforce my view that Brent will trade in a $90-110 
						range in next six month. The LNG market is also tight 
						though US Henry Hub natural gas prices could well fall 
						10 per cent as shale/lower power generation pressures 
						prices lower.<br>
						<br>
						The macroeconomic storm clouds suggest lower metal 
						prices, particularly for industrial growth/constructive 
						sensitive copper, nickel, aluminium and lead. Even 
						Chinese PMI data have fallen for three consecutive 
						months, import volumes have softened and property 
						markets have weakened. This means tonne range of the 
						London Metal Exchange. China has begun to reduce copper 
						buying and inventories are far too high. Dr Copper, with 
						its symbolic Ph.D in global economics, is flashing a 
						bearish signal for all industrial metals. Aluminium 
						prices are also headed lower. The spot market is only 
						temporarily tight due to LME arbitrage and financing 
						deals even though global smelters have increased their 
						output, raising the prospect of an aluminium glut.<br>
						<br>
						Gold’s bull market, while intact, is skating on thin ice 
						as global economic growth has stalled, not moved into 
						recession. The EU will not allow the destruction of the 
						Euro, let alone the German and French banking system or 
						Eurozone sovereign debt markets. At current levels, 
						bullion demand in Asia is soft while the Fed has refused 
						to expand its balance sheet in a third round of 
						quantitative easing. Yet real interest rate are still 
						negative, a positive for gold, while emerging markets 
						still use price weakness to add to their bullion 
						reserves. Thailand, Iran and Taiwan have recently bought 
						gold to diversify central bank reserves. Gold could well 
						trade in the $1500 – 1800 range in the next three 
						months. Silver prices have plunged since their April $49 
						peak but are probably headed down to the $28-32 range.<br>
						<br>
						I believe investors should bottom fish for oil/gas 
						shares only when Brent trades at the lower end of its 
						$90-110 range. For instance, Exxon Mobil (symbol: XOM on 
						NYSE) is the world’s leading integrated oil supermajor 
						with the longest life reserves and a cash cow chemicals, 
						refining, marketing business. Qatari LNG alone is worth 
						$20 a share. I believe Exxon will trade in a 68-78 range 
						at Brent $90. Schlumberger (SLB), the world’s largest 
						oil services firm, has been gutted in 2011 from its $95 
						peak in February 2011. I believe Schlumberger is a 
						compelling buy in the 56-58 range for a $75 target as 
						offshore West Africa and GCC/Russian/Chinese state owned 
						oil company capex will anchor its EPS growth. The 
						world’s major oil companies are all desperate to boost 
						their reserves and Schlumberger is the class act in 
						seismic/oilfield management technologies. European oil 
						companies offer compelling dividend, particularly 
						Total’s 7 per cent. I also believe the political turmoil 
						in Egypt has created an opportunity in Apache Petroleum, 
						which is a takeover candidate for Big Oil. Anadarko 
						(symbol APC on NYSE) has struck offshore paydirt with a 
						10 trillion cubic feet gas reservoir in Mozambique. 
						These gas reserves are higher than Britain and are 
						destined for Japan and India, as Mitsui and Bharat 
						Petroleum are both strategic partners. In gold miners, 
						Newmont Mining has the best assets but I believe the 
						shares are fully valued at 64, even though Newmont 
						boosts 94 million ounces of gold. Below 50, Newmont 
						would offer a better risk-reward calculus.</font><font face="Verdana" size="4" color="#C00000"><b><br>
