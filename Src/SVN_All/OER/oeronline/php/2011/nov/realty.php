						<td valign="top">
						<p align="justify" style="margin: 3px 10px">
						<font face="Verdana" size="4" color="#C00000"><b>Trendy 
						yet durable<br>
						</b></font><font face="Verdana" size="1">
						<span class="style14"><strong style="font-weight: 400">
						<br>
						</strong></span></font><b>
						<font face="Verdana" style="font-size: 9pt">
				<img border="0" src="images/nov-realty1.jpg" style="border: 1px solid #808080" class="style4" hspace="8" vspace="4" width="200" height="293" align="right"></font></b><span class="style14"><strong><font face="Verdana" style="font-size: 9pt">Ramesh 
						Tainwala, President – Asia Pacific and Middle East, 
						Samsonite makes a case for products that are 
						fashionable, offer functional excellence and have a 
						shorter shelf life, in a chat with Mayank Singh<br>
						</font></strong><font face="Verdana" size="1">
						<strong style="font-weight: 400"><br>
						</strong><strong>Is the Middle East market a new focus 
						area for Samsonite?<br>
						</strong><strong style="font-weight: 400">It is true 
						that we have started looking at this part of the world 
						more seriously and the reasons are pretty obvious. 
						Mature markets like Europe and the US have fewer 
						opportunities and future growth has to come in Asia. In 
						Asia, China and India are already seen as strong 
						emerging markets. The Middle East is one market that 
						will become more important in the days to come. Till 
						now, consumer product companies were looking only at the 
						UAE market, but now there is a realisation that there is 
						more to this part of the world than Dubai. There are 
						other countries with big populations such as Egypt, 
						Saudi Arabia and Iran, which will become big drivers of 
						growth. Even countries with a smaller population like 
						Oman, Bahrain and Qatar have a high GDP. A younger 
						population coupled with disposable income leads to more 
						travel and this helps our business. My visit to the 
						Middle East and Oman is driven by the desire to 
						understand this market better in terms of demographics, 
						consumer preferences, choices and styles.<br>
						<br>
						</strong><strong>Is the Samsonite customer in the Middle 
						East different from a buyer in other parts of the world 
						and are you looking at customising products for this 
						region?<br>
						</strong><strong style="font-weight: 400">The 
						fundamental needs of a customer across geographies 
						remain the same – everyone wants a lightweight bag which 
						is strong and offers various features. Apart from these 
						givens, there are smaller nuances which, if addressed, 
						can attract customers. For example, I have noticed that 
						people in the GCC like gold and so it may be a good idea 
						to bring in a bling element in our products, the fabrics 
						can have a few embellishments. Such effort gives the 
						customer the impression that we are taking them 
						seriously. We will hold our first design meet in the 
						region in February 2012 in Dubai. We are inviting our 
						designers to the region and they will visit various 
						countries here as a part of an inspiration tour.<br>
						<br>
						</strong><strong>How much does the Middle East market 
						contribute to your global sales?<br>
						</strong><strong style="font-weight: 400">It’s still a 
						small market for Samsonite, but more importantly it is 
						one of our fastest growing markets. This market has seen 
						a CAGR of 40 per cent during the last three years. We 
						are the unequivocal number one in the regional market 
						with American Tourister being the No.2 in some markets. 
						On an average American Tourister is the No.3 brand in 
						the market and our aim is to make the brand the No.2 
						product in the market.<br>
						<br>
						</strong><strong>Samsonite has gone into accessories 
						like wallets, eye wear and hand bags. How will such 
						extensions help the brand? <br>
						</strong><strong style="font-weight: 400">We want to get 
						into close to body products for a number of reasons – 
						one, the manufacturing process of these are similar to a 
						luggage so there are synergies. Secondly, as a category 
						luggage is bought after careful thought and planning and 
						not on an impulse. A number of times people walk across 
						our stores, but they are not inclined to walk in as 
						luggage is not a very browsing friendly product. On the 
						other hand close to body products like office bags, 
						wallets etc, have shorter life cycles and can trigger 
						impulse buying. These products have been yielding very 
						good results for the company – traveller products 
						contribute almost a third of our revenues and over the 
						next three years we believe that these can contribute 
						almost 50 per cent of our revenues.<br>
						<br>
						</strong><strong>As you get into close to body products, 
						will you be competing with fashion brands likes Gucci
						<br>
						and Prada?<br>
						</strong><strong style="font-weight: 400">For over 100 
						years we have been known for durability, reliability and 
						giving consumers excellent functional products. At the 
						same time we do not want to give consumers a boring 
						product. A number of times, luxury products like Gucci 
						and Prada are usually carried as showpieces rather than 
						for use. But in our case, Samsonite wants to deliver 
						good looking products that also offer functional 
						excellence. An element of fashion is also being 
						introduced as we are looking at products with shorter 
						life cycles.<br>
						<br>
						</strong><strong>Samsonite is known for its durability 
						while fashion products have shorter life cycles. Will 
						this dilute your brand proposition?<br>
						</strong><strong style="font-weight: 400">We are working 
						on a strategy wherein we don’t want to sell anything for 
						more than one year. The idea is that when a customer 
						walks past our store he should see a new range and feel 
						that he has an old product. If we keep selling an old 
						luggage, few will have an incentive to buy anything new. 
						The same applies everywhere, there was a time when 
						Mercedes changed the E-series once in six years now 
						there is a new E-series every two years. It’s the same 
						people who buy things again but we need to give them a 
						reason to buy our products.<br>
						<br>
						</strong><strong>What is the average life cycle of a 
						Samsonite luggage and has the life cycle been coming 
						down in recent years?<br>
						</strong><strong style="font-weight: 400">When I joined 
						Samsonite 15 years ago we used to sell a product for 10 
						years but now we want to sell a product for only one and 
						a half years. A product should be phased out at the peak 
						of its life cycle so that people have a fond memories of 
						it. Next time when the customer asks for it we should be 
						in a position to say that we do not have that range but 
						have something better to offer. Shorter product life 
						cycles would require better inventory management and 
						augmentation of our design capabilities.<br>
						<br>
						</strong><strong>Will such investments affect the 
						pricing of your products?</strong><strong style="font-weight: 400"><br>
						No, I do not think that it will affect prices.<br>
						<br>
						</strong><strong>Samsonite has set up a research and 
						development hub in Nasik, India. What are the reasons 
						that has made the company set up this centre and what is 
						being used for?<br>
						</strong><strong style="font-weight: 400">India has an 
						abundance of engineering talent and technicians and this 
						is a key driver for setting up an R&amp;D centre in Nasik. 
						In addition, the country has a fairly good intellectual 
						property regime which gives us the confidence that our 
						technology is well protected in India. China too has a 
						lot of capable engineers and technicians, but the 
						intellectual property regime is not well regulated and 
						this reduces our comfort levels. Apart from India, we 
						have manufacturing and R&amp;D centre in Belgium.<br>
						<br>
						</strong><strong>As you introduce products with shorter 
						life cycles, are you also looking at certain changes on 
						the retail side?<br>
						</strong><strong style="font-weight: 400">To be 
						successful it is not important how one manages success 
						but rather how one manages failure. If you are fearful 
						of failure you will never succeed. On the retail side if 
						we have 90 hugely successful stores, then we should be 
						ready to admit that there are 10 of them that are not 
						doing well. Usually, we get to know whether a store is 
						doing well or not within three to six months and our 
						business plan should provide for such failures. When we 
						make a mistake it is important to own it and to rectify 
						it.<br>
						<br>
						</strong><strong>Is the unorganised sector in emerging 
						economies a threat to established brands like Samsonite?<br>
						</strong><strong style="font-weight: 400">Now a days, 
						people do not have time to look around for unbranded 
						products and so the unorganised sector is losing market 
						share across categories. Customers prefer to go in for a 
						brand as it delivers on a promise that the brand stands 
						for. <br>
						</strong></font></span></td>
