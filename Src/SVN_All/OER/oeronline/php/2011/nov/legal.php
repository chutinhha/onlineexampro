						<td valign="top">
						<p align="justify" style="margin: 3px 10px">
				<font color="#CC3333" size="4" face="Verdana"><b>On concession 
				agreements<br>
				</b></font><font face="Verdana" size="1"><br>
				<font color="#808080"><i>By James Harbridge<br>
				</i></font><br>
				</font>
				<font face="Verdana" style="font-size: 9pt; font-weight: 700">A 
				concession agreement is a contract concluded by the government 
				or its representatives with third parties with the aim of 
				exploration, excavation, development and extraction of petroleum 
				substances</font><font face="Verdana" size="1"><br>
				<br>
				<br>
				Earlier this year, a new Oil and Gas Law was promulgated by 
				Royal Decree 8/2011, replacing the old Oil and Minerals Law. It 
				covers a number of important issues including concession 
				agreements, boundaries of the concession area, environmental 
				issues and manpower protection.<br>
				<br>
				A ‘Concession Agreement’ is defined, in the Law, as a contract 
				concluded by the government or its representatives with third 
				parties with the aim of exploration, excavation, development and 
				extraction of petroleum substances (or any of these activities 
				independently).<br>
				<br>
				According to Article 7, none of the above activities may be 
				carried out without a concession agreement which needs to be for 
				a specific period and which cannot come into effect unless 
				ratified by a Royal Decree. To be granted concession rights, the 
				prospective concessionaire must be technically and financially 
				qualified in accordance with the criteria set by the Ministry of 
				Oil and Gas.<br>
				<br>
				Before signing the concession agreement, the ministry may ask 
				the prospective concessionaire to provide a financial security 
				of not less than 2 per cent and not more than 5 per cent of the 
				value of the agreement. The security must be valid for the 
				entire term of the agreement as a guarantee that the obligations 
				stipulated in the agreement will be performed. If the 
				prospective concessionaire breaches his obligations, the 
				security will be forfeited.<br>
				<br>
				The Law requires the concession agreement to specify the 
				parameters of the concession area, which must be within at least 
				three kilometres of the Omani borders if the concession area is 
				located close to the borders of the Sultanate.<br>
				<br>
				In any event, no operations may be carried out within (200) 
				metres of towns, villages, roads, mosques, graveyards, 
				archeological sites, protected natural sites, dams, falajs, and 
				wells; or within (500) metres of the lands and camps of the 
				Ministry of Defence and Sultan’s Armed Forces, unless 
				specifically authorised by these entities.<br>
				<br>
				The agreement must also specify the term of the agreement, the 
				rights and obligations of each of the two parties, rules 
				governing the discovery of oil substances in commercial 
				quantities, management of the operations, safe production rate 
				in the concession area, Omanisation and training requirements, 
				the steps for preserving the environment, health, security and 
				safety, rules for the assignment and surrender of the area, and 
				the rules governing dispute resolution.<br>
				<br>
				The agreement must provide for mechanisms whereby the ministry 
				will supervise and monitor the implementation of the agreement. 
				The concessionaire will be required to take out insurance 
				against all risks in respect of fixed and movable assets used in 
				the operations, and against civil liability arising from any 
				damage caused to individuals, property, public safety or the 
				environment as a result of his operations in the area.<br>
				<br>
				After discovery of oil substances in commercial quantities and 
				prior to exploitation, the concessionaire will be required to 
				develop a comprehensive plan which ensures security and safety 
				in the concession area and which meets all applicable legal 
				requirements in this regard. The Concessionaire shall coordinate 
				with the Royal Oman Police to approve that plan, which has to be 
				renewed every two years. <br>
