				<font face="Verdana" size="1">Mazda Motor Corporation, in 
				collaboration with Sumitomo Metal Industries and Aisin Takaoka 
				Co. has become the first automaker to successfully develop 
				vehicle components using 1,800 MPa ultra-high tensile steel. The 
				super strength steel will debut in the all-new Mazda CX-5 
				crossover SUV that will commence its global launch in early 
				2012. <br>
				<br>
				Mazda’s new production technology uses 1,800 MPa ultra-high 
				tensile steel to fabricate bumper beams, which fit inside the 
				front and rear bumpers and mitigate damage in the event of a 
				collision. The bumper bars are 20 percent stronger and 4.8 
				kilograms lighter than previous versions and are a key part of 
				Mazda’s next-generation, lightweight and highly rigid vehicle 
				architecture. The new body architecture was developed as part of 
				Mazda’s breakthrough Skyactiv Technology programme and 
				incorporates a new energy absorbing structure as well as an 
				expanded use of high tensile steel to reduce weight.</font>&nbsp;</td>
