						<p align="justify" style="margin: 3px 10px">
				<b><font face="Verdana" size="4" color="#C00000">By Oliver 
				Cornock</font></b><p align="justify" style="margin: 3px 10px">
				<font size="1" face="Verdana"><br>
				In the late 1970s, Prince Mohammed al Faisal of Saudi Arabia, 
				nephew of then-King Khaild, had an novel plan to solve his arid 
				country’s water woes: icebergs. The Prince, then head of the 
				Kingdom’s desalination programme, was convinced that his country 
				should start commissioning boats to tug polar icebergs (some as 
				large as Belgium) to the Kingdom’s shores. With this water, the 
				government would be able to boost the country’s agricultural 
				industry.<br>
				<br>
				Although these iceberg plans never materialised, the Kingdom 
				proceeded to find water elsewhere – in its aquifers. The water 
				was enough to develop large scale agriculture and in particular 
				wheat production.<br>
				<br>
				By the 1990s, the country was actually a net-exporter of wheat. 
				Desert agriculture is not cheap, however. Farms required 
				financial support from the government and increasingly large 
				amounts of non-renewable water resources. Saudi officials now 
				recognise that the environmental and economic costs are simply 
				too high for sustainable large-scale agriculture and in 2008 
				announced they would phase out the programme. “The reason is 
				water resources,” an official wishing to remain anonymous told 
				Reuters.<br>
				<br>
				Qatar, like its neighbour, has neither plentiful water reserves 
				nor many areas suitable for crop cultivation. The World Bank 
				estimates that about 1 per cent of the country’s land is arable. 
				As a result, the country relies on imports for the vast majority 
				of its food needs. The Qatar National Food Security Programme, a 
				governmental organisation, was created in 2008 to reduce 
				reliance on imports.<br>
				<br>
				The task is a challenging one. Qatar’s population has more than 
				doubled in the last ten years, according to the Qatar Statistics 
				Authority. With an increasing number of mouths to feed, food 
				demand is on the rise. Between 2011 and 2015, Qatar’s overall 
				food consumption is set to grow at a rate of 6.3 per cent, 
				according to a report published by Alpen Capital. With scarcely 
				any domestic production, the State has little control over the 
				price of food, which has steadily risen over the past decade. 
				August 2011 prices, for example, were 26 per cent higher than 
				those of August 2010, according to the UN Food and Agriculture 
				Organisation’s (FAO). FAO, although “cautiously optimistic” that 
				prices will come down from 2010-11 levels, predicts higher 
				prices in the long term: “The Outlook maintains its view in 
				recent editions that agricultural commodity prices in real terms 
				are likely to remain on a higher plateau during the next decade 
				compared to the previous decade,” it said in the OECD-FAO 
				Agricultural Outlook 2011-2020.<br>
				<br>
				Higher food prices are only the beginning of the challenge. The 
				Qatar Food Security Programme estimates 75 per cent of the food 
				costs in Qatar are the result of non-food components, including 
				packaging, marketing and transportation. Import prices are 
				especially sensitive to these and other external factors. Last 
				spring in Australia, one of the Qatar’s largest meat suppliers, 
				a combination of flooding and a strong Australian dollar raised 
				prices and restricted supply. Although Qatar was able to find 
				alternative sources, Doha hopes to invest in domestic food 
				production to reduce its exposure to external factors.<br>
				<br>
				Some technologies being developed could offer solutions for 
				home-grown produce in the country. One project in the pipeline 
				is an agricultural city that would concentrate food growers and 
				researchers in one place. “It is in the early stages,” Mohamed 
				Ahmed al Obaidly, head of the agricultural and environment 
				committee at the Qatar Chamber of Commerce and Industry, told 
				Reuters at a conference in Doha, “but it would be a large 
				investment, in the billions of dollars.” Techniques used 
				elsewhere to grow crops in arid areas, such as high-efficiency 
				irrigation and advanced water recycling, could be useful in 
				Qatar as well.<br>
				<br>
				Still, the security of home-grown food can come at a high 
				economic and environmental price, as the Saudi wheat programme 
				demonstrated. Another option might be developing a broader pool 
				of trade partners by leveraging its existing industries. Qatar, 
				for example, has the potential to develop strong trade 
				relationships with food-producers who could benefit from the 
				State’s growing fertiliser production. The Qatar Fertiliser 
				Company (QAFCO), established by the government and foreign 
				stakeholders in 1969, produces urea and ammonia, both crucial 
				elements of fertiliser. Two new plants, QAFCO-5 and QAFCO-6, are 
				set to be completed in late 2011, according to the company’s web 
				site. Upon completion, the plants are set to make QAFCO the 
				world’s largest single-site producer of ammonia and urea.<br>
				These fertiliser production increases come in the context of 
				rising global demand. At the second-annual GPCA Fertiliser 
				Convention in Dubai last September, Brazil, Russia, China, India 
				and a group of African nations called for increased fertiliser 
				production to ensure food supplies keep up with population 
				growth. Qatar’s growing fertiliser capacity, then, could open 
				opportunities for future trade relationships with major food 
				producers, whose demand for fertilisers is set to rise. Qatar 
				could help raise crop yields elsewhere, which would </font>
				<font face="Verdana"><b><font size="1">in</font></b></font><font size="1" face="Verdana"> 
				turn allow the State to share in those higher yields.<br>
				<br>
				The key here seems to be balance. In the short term, a broad 
				base of suppliers and effective use of the State’s economic 
				resources could do much to ensure that the country has a steady 
				supply of food in the short term. Meanwhile, support for 
				research on desert farming could increase domestic crop 
				production in the long term. Although there may not be a single 
				solution to Qatar’s food security challenge, a mix of 
				locally-sourced food, foreign land-holdings and trade agreements 
				could offer the country a number of tools in its quest to feed 
				its population. <br>
				</font> </td>
