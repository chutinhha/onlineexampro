						<td valign="top">
						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">
						Reaffirming a relationship</font></b><p align="justify" style="margin: 3px 10px">
				<font face="Verdana"><span class="style5"><strong><br>
				<span style="font-size: 9pt">Samer Majali, CEO, Gulf Air, was in 
				town to announce the launch of the airline’s most </span></strong></span>
				</font><font class="style11"><font face="Verdana" size="1"><b>
				<font style="font-size: 9pt">recent</font></b></font></font><font face="Verdana"><span class="style5"><strong><span style="font-size: 9pt"> 
				promotional initiative ‘Top Trips’ with discounted fares on 
				flights from Muscat to Europe and Middle East destinations. 
				Excerpts of an interview with Visvas Paul D Karra<br>
				</span></strong></span>
				<br>
				</font><font face="Verdana" size="1">
				<img border="0" src="images/nov-aviation1.jpg" width="120" height="406" style="border: 1px solid #808080" class="style4" align="right" hspace="6" vspace="15"><b>Please 
				give a brief background about yourself</b><br>
				I am an aeronautical engineer with a masters degree in transport 
				management from the UK. I started my career in Royal Jordanian 
				and worked there for 30 years, the last eight of them as CEO of 
				that airline. Then I moved to Gulf Air in 2009 where I was 
				involved in the restructuring of the company. I was also 
				chairman of IATA World Carriers in 2008-09 and a member of the 
				Arab Air Carriers Organisation. Besides that, I have been a 
				board member of the Central Bank of Jordan.<br>
				<br>
				<b>Can you give details of Gulf Air’s restructuring process?</b><br>
				We have been restructuring the airline, trying to commercialise 
				it and bring it to a breakeven situation within a three- to-four 
				year period. And we have done excellent progress in 2010. 
				Hopefully, after the incidents that happened in the early part 
				of this year in Bahrain, we resumed our march of commercialising 
				the airline, essentially trying to bring the airline into a 
				position where its revenues cover the costs. We have done this 
				by making major improvements in our network, modernising our 
				fleet in terms of supply and demand and improving the quality of 
				service in terms of the software and hardware. Besides this, we 
				have concentrated on time punctuality and finally reduction in 
				costs.<br>
				<br>
				<b>What have been the major learnings in the restructuring 
				process?</b><br>
				The restructuring process can be divided into internal and 
				external. Internally, I think every problem has a solution as 
				long as you focus primarily on your people; and as long as you 
				put the right team in the place, motivate your staff in the 
				right direction, take care of them and be transparent, you can 
				achieve miracles. So internally the primary focus is your 
				people.<br>
				Externally, it is getting your owners to focus on your problem 
				and what you want from them and what they want from you. So you 
				need clarity from your shareholders on what they want you to 
				deliver and what they have to deliver to you to make that 
				happen.<br>
				<br>
				<b>How many passengers do you fly on an average in a year and 
				when do you plant to break even?</b><br>
				We fly about 5.5 million passengers annually. The initial plan 
				was to break even somewhere in the middle of 2012 but then the 
				disturbances in Bahrain slightly slowed our strategy, 
				nevertheless we are still on track; maybe we are late by six 
				months but hopefully in the coming two years, we hope that we 
				will have a break even situation.<br>
				<br>
				<b>What is the present seat load factor?<br>
				</b>It is in excess of 74 per cent year round, which is very 
				healthy and we want to average it in the high 70s next year. As 
				for cargo, we are not a major cargo carrier as the nature of our 
				airplanes; and the nature of our route network has made us 
				slightly change our strategy for cargo concentrating more on 
				courier traffic and high value cargo. Because we have smaller 
				airplanes and high frequency network, many shippers wants to get 
				their goods from A to B very quickly and very reliably. Moreover 
				it is not only in terms of the high frequency but also because 
				of the destinations – we go where other carriers don’t. Much 
				like our passenger, who want to go to unique destinations, even 
				the cargo we carry wants to go to a unique destination. And this 
				is what we do in this region.<br>
				<br>
				<b>How much impact did the protests in Bahrain have on Gulf Air?<br>
				</b>I think the general issues that happened in the Arab world 
				had an effect on all of the airlines. Obviously, in areas where 
				there was more activity, the airline got affected more, not only 
				the incoming traffic to the region in terms of visitors but also 
				people leaving the region and travelling within the region. So 
				that was one effect that we saw in the first half of 2011. The 
				other effect was the increase in the fuel prices which was quite 
				considerable. From any airline’s perspective it was not a good 
				start for 2011 but atleast we are recovering quite well and we 
				are moving ahead.<br>
				<br>
				<b>Bahrain has been a financial capital of the region. Did the 
				political crisis have an effect on your premium segment?<br>
				</b>Actually no. The financial crisis purely did not have an 
				impact but in fact the global depression had an impact on the 
				global travel pattern. But Bahrain was not affected by the 
				financial crisis to the extent that other countries were.<br>
				<br>
				<b>Can you talk about your fleet strength?<br>
				</b>At the moment, from a destination point of view, we have 
				been increasing steadily and we hope to go up to 55-60 
				destinations next year. In terms of the fleet, we are currently 
				having between 30-35 airplanes because we are phasing out older 
				airplanes and inducting newer airplanes into the fleet. We hope 
				to reach to the level of 40 airplanes by the end of next year. 
				Essentially we have the wide bodied long range airplanes which 
				are being currently renovated with new interiors. The second 
				element is the narrow body airplanes. These are brand new Airbus 
				airplanes. Within the next 10 months we will have 26 narrow body 
				airplanes with a combination of 319s, 320s and 321s. And we have 
				some regional jets.<br>
				<br>
				<b>What about your destinations?<br>
				</b>Our primary investment is increasing the destinations and 
				frequency of operations to the current destinations within the 
				region itself. We want to attain a position as the largest 
				carrier in terms of the number of points and the number of 
				flights in a 3-hour radius in the Gulf region itself while 
				maintaining our long range operations to Europe and the Far 
				East. We connect passengers between various points via Bahrain 
				and secondly we connect passengers to various regional points 
				from Bahrain.<br>
				<br>
				In the region, we are trying to cover the primary and the 
				secondary airports of most countries. So in Saudi Arabia, we are 
				having six to seven destinations. In Oman we are looking at 
				Salalah, in Yemen we are flying to two points and in Iraq and 
				Iran four points. We are trying to cover and blanket the whole 
				region’s primary and secondary points with our airline.<br>
				<br>
				Gulf Air has been charged with issues like poor service to 
				passengers on previous occasions. Your comments.<br>
				<br>
				We haven’t received any such reports (expresses surprise). But 
				we do take corrective action immediately on any reports. We 
				continuously monitor the performance of our staff and have 
				training sessions for them to prevent any discriminatory service 
				and this is an ongoing process to any service industry. But they 
				are our passengers in the end and it is important to take care 
				of them because they are paying.<br>
				<br>
				<b>Gulf Air has unveiled a series of commercial initiatives 
				tailored to the Omani market. What is the aim behind this?<br>
				</b>Oman is a very important market for us. Traditionally, when 
				Oman was a shareholder in Gulf Air, a strong relationship 
				existed between the leaderships of both countries and between 
				its people, businesses and so on. Even when the shareholding 
				broke up, Gulf Air maintained its strong links with Oman and the 
				development of Oman. The Omani market is growing rapidly with 
				new businesses and enterprises and the services sector in 
				general is also improving, more specifically tourism. And we 
				want to participate in that growth. <br>
				<br>
				<b>What about your carbon footprint?<br>
				</b>As an airline, of course we have to reduce it. If you notice 
				actually, the primary strategy in introducing new airplanes is 
				to consume less fuel and consequently reduce the environmental 
				damage. But within the company we are taking operational steps 
				to reduce fuel consumption. I mean airlines have a natural 
				incentive to produce less carbon because that is directly 
				associated with fuel and that is costs. Don’t worry about the 
				carbon footprint of airlines. </font>
