		<b><font face="Verdana" size="2" color="#CC3300">Reuters agrees to US$17.6bn Thomson buyout</font></b><font face="Verdana" size="1"><br>
<img border="0" src="WORLD_SCAN02.jpg" align="right" hspace="8" style="border: 2px solid #EFEFE9; " vspace="4">Thomson 
		has won approval for its US$17.6-billion takeover of Reuters from the 
		British company’s editorial watchdog, but the deal now faces scrutiny 
		from anti-trust regulators and unions unhappy about expected job cuts. 
		The renamed Thomson-Reuters Corp. will reduce the number of major 
		companies providing financial data, news and trading systems to just two 
		and vault it slightly ahead of the current market leader, privately held 
		Bloomberg LP.<br>
		<br>
		The backing of trustees of the Reuters Founders Share Company was a 
		crucial first step in creating the world’s largest financial news 
		provider. The trust, which controlled what is known as a “golden share”, 
		was set up when Reuters listed on the London Stock Exchange in 1984 to 
		safeguard editorial independence. “We believe that the formation of 
		Thomson-Reuters marks a watershed in the global information business,” 
		said Swedish businessman Pehr Gyllenhammar, chairman of the trustee 
		company, whose directors have the power to order any shareholder whose 
		holding exceeds 15 per cent to reduce stake. The Thomson family of 
		Canada, which will have a 53 per cent majority stake, agreed to adopt 
		the Reuters Trust Principles as long as it controls Thomson-Reuters, the 
		companies said. Reuters Chief Executive Tom Glocer, who will head the 
		combined company, said that some “realignment” is likely to occur to 
		meet a goal of US$500 million in cost reductions over three years. But 
		he played down the prospect of large-scale job cuts, pointing to the 
		fact that Reuters has already made cost savings totalling around US$1.8 
		billion over the past five years.</font><p align="right" style="margin: 3px 10px"><br>
