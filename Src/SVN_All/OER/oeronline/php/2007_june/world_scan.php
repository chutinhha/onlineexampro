		</font><b><font face="Verdana" size="2" color="#CC3300">Daimler-Chrysler marriage ends<br>
		</font></b><font face="Verdana" size="1">Private equity firm Cerberus will buy a majority of DaimlerChrysler’s 
		struggling Chrysler Group for $7.4 billion, a fraction of the 
		US$36-billion paid nine years earlier to create the transatlantic car 
		union. Cerberus Capital Management gets an 80.1 per cent stake in 
		Chrysler and its related financial services business, DaimlerChrysler 
		said, ending what was billed as a marriage made in heaven but one that 
		never lived up to the name.<br>
		<br>
		The deal, months in the making and set to close in the third quarter of 
		the year, puts a major US automaker in the hands of a private equity 
		group for the first time. “Cerberus is the right strategic buyer for 
		Chrysler, with a long-term commitment to Chrysler’s growth and success. 
		They are committed to working constructively with both union leadership 
		and Chrysler’s management team to help Chrysler realise its full 
		potential,” Chrysler President Tom LaSorda said.<br>
		<br>
		He said the deal would not trigger job cuts beyond the 13,000 Chrysler 
		had announced in February, when it unveiled a US$1.5-billion 2006 
		operating loss as customers, spooked by high fuel prices, fled from its 
		portfolio of pickup trucks and sport utility vehicles.<br>
		<br>
		The accord calls for Chrysler to retain billions of dollars in pension 
		and healthcare obligations for its workers and will result in a net cash 
		outflow of US$677 million for DaimlerChrysler, now the world’s 
		fifth-biggest carmaker. The German company – whose name will change to 
		Daimler AG if shareholders approve – will contribute another US$875 
		million to cover long-term liabilities at Chrysler, it said.</font><hr color="#C0C0C0" width="80%" size="1">
