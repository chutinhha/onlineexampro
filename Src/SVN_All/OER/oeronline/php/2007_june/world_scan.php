</font><b><font face="Verdana" size="2" color="#CC3300">US Warburg Pincus to buy Bausch &amp; Lomb for US$3.67bn</font></b><font face="Verdana" size="1"><br>
<img border="0" src="WORLD_SCAN01.jpg" align="right" hspace="8" style="border: 2px solid #EFEFE9; " vspace="4">Eye-care 
company Bausch &amp; Lomb Inc, still dealing with widespread product recalls that 
have delayed financial reports, has said it had agreed to be acquired by private 
equity firm Warburg Pincus for about US$3.67 billion. Under the deal, Warburg 
Pincus will acquire all outstanding shares of Bausch &amp; Lomb common stock at 
US$65 per share in cash. It will also assume about US$830 million in debt. The 
offer was at a 5.7 per cent premium to where Bausch &amp; Lomb shares closed on the 
day the deal was announced. However, the eye-care company’s share prices had 
been on a sharp ascent for several weeks due to rumours that it would be the 
target of a leveraged buyout.<br>
<br>
Warburg Pincus’ offer is nearly 28 times the estimated 2007 earnings per share 
for Bausch &amp; Lomb, and 9.5 times the estimated 2007 earnings before interest, 
taxes, depreciation and amortisation, based on data from Reuters Estimates.</font><hr color="#C0C0C0" width="80%" size="1">
