		<b><font face="Verdana" size="2" color="#CC3300">EADS suffers US$13.5bn loss on Airbus woes</font></b><font face="Verdana" size="1"><br>
European aerospace giant EADS suffered a $13.5 million first-quarter net loss, 
reflecting restructuring costs at its crisis-hit aircraft unit Airbus. EADS said 
its first quarter performance this year had been affected by the provision of 
US$926 million to finance a vast restructuring at Airbus, dubbed Power8, that 
will result in the loss of about 10,000 jobs.<br>
<br>
Operating earnings before exceptional items and goodwill writedowns fell by 89 
per cent from the first quarter of 2006 to US$120 million. Airbus, bedevilled by 
costly production and delivery delays in its flagship A380 superjumbo programme, 
reported a first-quarter operating loss of US$93 million after net profit of 
US$920 million in the same period last year.</font><hr color="#C0C0C0" width="80%" size="1">
