		<b><font face="Verdana" size="2" color="#CC3300">ADB fails to reach consensus on reform agenda</font></b><p align="justify" style="margin: 3px 10px">
<font face="Verdana" size="1">The Asian Development Bank faces an uncertain future after failing to reach a 
clear consensus on how to reform in response to Asia’s rapid but increasingly 
uneven economic growth. The 67-member ADB wrapped up its annual meeting in Kyoto 
recently amid disquiet among many members about proposals for the multilateral 
lending institution to focus more on the needs of middle-income countries. While 
delegates were generally in favour of an overhaul to ensure the bank’s continued 
relevance, many urged the ADB not to lose sight of its original mandate to lift 
millions of Asians out of poverty. “While a larger number of countries in the 
region will become middle-income countries, some will remain low-income 
countries,” Cambodia’s ADB governor Keat Chhon warned. “There will still be a 
significant number of poor in the region. Our view is that the ADB should 
continue to focus on poverty alleviation in these low-income economies.”
