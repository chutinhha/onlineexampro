</font><b><font face="Verdana" size="4">The Woods Touch<br>
</font><font face="Verdana" size="1"><br>
The Masters Tournament at Augusta National attracted 100 players this year. 
Apart from five amateurs and 18 first-time participants, the Masters saw 
Mickelson and six of the other nine winners from the PGA Tour this season, 
including former Masters champions Vijay Singh and Tiger Woods. Our story is 
about Tiger Woods – Golf’s golden boy!<br>
<br>
<br>
</font></b><font face="Verdana" size="1">
<img border="0" src="golf.jpg" align="left" hspace="6" vspace="4">Tiger 
Woods, at 31, has won about a dozen Masters and four jackets and was the most 
looked-out-for player at the Masters. Nothing unusual. He is where Jack Nicklaus 
and Arnold Palmer were when they were in their 40-somethings. Only that he has 
done it 10 years earlier. He is at the brink of opening a new chapter in his 
life. It is not just about becoming a first-time father in the near future but 
also about his passion called golf and his Midas touch.<br>
<br>
Both the man and the game have undergone almost a mystical change. Since Tiger 
Woods, golf has become cool and hi-tech. So are the purses, which are exploding 
now. He is on the fast track to become the world’s first billionaire sportsman.<br>
<br>
Woods’ 12 professional major titles include the last two contested – the British 
Open and the PGA Championship. He now eyes Jack Nicklaus’ mark of 18 majors and 
mathematical probabilities indicate that he should be approaching the Golden 
Bear’s target by 2012. Woods has influenced every aspect of the game in the most 
amazing way. His catalysing presence can be seen in the way players at the top 
level prepare for the competition and more significantly it is visible on the 
hitherto-disinterested population. More and more non-golf fans are watching the 
sport.<br>
<br>
Look at top-of-the-line courses like Augusta National Golf Club that have become 
the golfer’s pilgrimage, influenced by players like Woods. In 1997, Augusta 
National measured 6,925 yards – one of 22 layouts on the PGA Tour that year less 
than 7,000 yards. The advertised yardage for the 71st Masters is 7,445 yards 
making it one of 20 layouts at least 7,300 yards long. Prize money on the PGA 
Tour amounts to about US$80 million. This year the tour will dole out more than 
US$266 million, including television revenues that are about eight times what 
they were when Woods became the champion.</font><p align="right" dir="ltr" style="margin-left: 10px; margin-right: 10px">
