<b><font face="Verdana" size="4">Keeping the battery fit</font></b><font face="Verdana" size="1"><br>
<b>Follow these simple instructions on how to maintain and change your car’s 
battery, and you’ll be moving again in no time <br>
</b>
<br>
<br>
<img border="0" src="spot2.jpg" width="200" height="175" align="left" hspace="6" vspace="4">Before even beginning these simple steps, heed the safety advice: The car’s 
ignition must be off! Your battery also contains a highly flammable solution, so 
you should work in a well-ventilated area. It is a good idea to wear protective 
eyewear and gloves as well.<br>
<br>
You don’t want to jump the gun on pronouncing your old battery ‘a goner’ without 
doing some checking first. If your battery has cracks in it, look no further. 
Order a new one. If not, take a look at the battery terminals before making a 
decision. The battery terminals are the positive and negative knobs that attach 
wires to the alternator. Sometimes corrosion on the terminals is the problem. 
Most places that sell batteries will test your battery for free and advise you 
if a new one is required or if a good charge is all you require. Removing the 
leads and cleaning the contacts may solve the problem.<br>
<br>
Weather can also cause battery problems. Open the vent caps to check if the 
solution inside is frozen. Moving your battery to a warmer place till the fluid 
thaws will get the battery running again. <br>
<br>
Once you’ve determined that you do need a new battery, first step of course is 
to remove the old one. Undo the clamp, which holds the battery in position, and 
then, with your wrench, carefully unfasten the battery bolt that holds the 
negative clamp, sliding the cable clamp off the terminal. Then do the same thing 
for the positive cable clamp. At this point, get a firm grasp on the battery and 
remove it from your car. Then spend a little time cleaning the terminal clamps 
and battery tray with a baking soda/water solution. Terminal clamps can also be 
cleaned with a wire-brush or coarse emery-paper. <br>
<br>
When buying the new battery, be sure to buy the correct size, output and 
terminal orientation for your car. Most battery sellers can advise you on this, 
as can your vehicle manual. Carefully position your new battery in the battery 
tray. Make sure that the positive and negative terminals are on the proper 
sides, and then screw on the bracket so that the battery is held in place. Put 
the positive cable clamp over the positive terminal and connect the negative 
cable in the same way. Then close the bonnet and start the car. You will almost 
certainly need to re-enter your radio security code at this point. <br>
<br>
The importance of this next step can’t be emphasised enough! Your dead battery 
is highly toxic and must be disposed of properly. You can take it to a service 
garage, but it is recommended that it should be taken to a recycling centre 
where they will make certain that the material is properly used again.<br>
