<font face="Verdana" size="1"><br>
</font><b><font size="4" face="Verdana">‘Enough liquidity in the market’</font></b><font face="Verdana" size="1"><br>
<b>It is now clear that the MSM is going to touch new heights in the days to 
come. The real question is how prepared is the Omani market for the deluge. P 
Aneel Kumar checks out wi</b><b>th Abdullah bin Salem Al-Salmi, chairman, Muscat 
Securities Market</b><br>
<br>
<img border="0" src="cover2.jpg" width="175" height="174" align="left" hspace="6" vspace="4"><b>With so many companies taking the MSM route, do you think the general market 
conditions are fit to absorb these money raising programmes?</b><br>
<br>
Basically, 
these programmes – whether public issues, funds or rights issues – provide huge 
opportunities for investors. It should be a win-win situation for both the 
companies as well as investors. On the one side, the companies are willing to go 
public with huge offerings and on the other, people are ready to invest. If the 
fundamentals are good, liquidity will automatically come. There is enough of 
liquidity in the market to absorb these issues. The timing of these issues is 
not at all contentious. Moreover, we’ll try to ensure that no two issues clash, 
so that the investors, particularly high net worth individuals and institutions, 
face no problem of liquidity. Otherwise too, this market is strong enough to 
absorb some big-ticket issues.<br>
<br>
<b>But this market doesn’t seem to be as active as its peers in the GCC region 
in terms of raising money…</b><br>
<br>
In terms of raising money, yes I agree. But in terms of its performance, we are 
far ahead of some of the GCC markets and also emerging markets. For instance, 
statistics show that MSM 30 Index rose by 14.5 per cent in 2006 compared to 
2005. This trend is reflected in market capitalisation of listed securities, 
which rose to RO6.220.8 billion at the end of 2006 compared to RO5.878.5 billion 
at the end of 2005. It is not the same case with other Arab securities markets. 
Moreover, in other GCC markets, the prices of shares are driven by speculation. 
In MSM, the price discovery has always been fair. However, there is no denying 
the fact that GCC markets have better liquidity, good depth in market and bigger 
capitalisation.<br>
<br>
<b>Do you think the MSM 30 Index really reflects the overall growth in Omani 
economy?<br>
</b><br>
To some extent, yes. But we are hoping for more, really. We’re hoping that the 
market will reflect the growth in various sectors. In the current situation, 
this is not the case, really. In the MSM Index, banking and investment is the 
leading sector whereas the entire economy is driven by the oil and gas sector. 
We don’t see the representation of this sector on the exchange. The other 
important sectors like tourism and realty are also not properly represented in 
the index. We are hoping more companies in these sectors will get listed on the 
MSM. We’re encouraging not just equity markets, but are asking these companies 
to use other fund raising instruments, where we feel we are lacking at the 
moment.<br>
<br>
<b>What significance you attach to the forthcoming issues given the fact fund 
raising in the primary market used to be a rare phenomenon a few years back?</b><br>
<br>
As I said earlier, it is going to be a win-win situation for investors as well 
as companies. We feel that there is enough liquidity in the market and these 
issues should match the liquidity available. As a result, market capitalisation 
will expand and overall market turnover will go up. These issues also add to the 
depth and breadth of the market. It will definitely attract more foreign 
investment and the participation of FIIs will just improve. We’ve already seen 
foreign participation growing from around 15 per cent to 22-23 per cent. <br>
<br>
<b>Which are the companies that might immediately go public?</b><br>
<br>
Galfar’s issue is in the pipeline. Oman Oil is talking about divesting its stake 
in Oman Oil Marketing. There is also talk of Takamul. We haven’t yet received 
any concrete proposal from these companies. We don’t have specific information 
on other issues.<br>
<br>
Actually, many were surprised when you allowed Bank Sohar to raise money in the 
primary market, as the bank had no past performance track record. As we hear, 
it’s going to be the same case with Takamul. What’s the idea behind allowing 
these start-ups to go public? In our markets, there are certain unique elements. 
That’s why we allow start-ups to go public. In other markets, you find an 
initiator, who initiates idea and takes it to venture capitalists, develops it 
and then goes public. There are no VCs or start-up funds in Oman as such. So, if 
a person or a group of individuals want to initiate a start-up, it is difficult 
for them to raise such huge capital. Therefore, we allow such start-ups to go 
public. But we insist that these initiatives have an underwriter who ensures 
such projects are studied thoroughly, the initiators have done a thorough 
homework and they are quite sure that the initiated project is a successful one. 
I don’t think any underwriter will take a risk in underwriting such a company 
unless he has a high degree of confidence about its success. Therefore, we try 
to balance the situation of lacking VCs by allowing these IPOs.<br>
<br>
<b>Can we expect any regulatory changes in the absence of any VCs and private 
equity firms in Oman?</b><br>
<br>
What we’re trying is to encourage VCs and PEs by inviting them into the market. 
In the 1980s and 1990s, the government used to support such start-ups, the best 
example is Oman Air. For some reasons, the government stopped this. So there is 
a vacuum in terms of sourcing funds. In this context, we are trying to encourage 
the government to set up a fund along the lines of VCs and PEs to help these 
start-ups. This will not only help the start-ups but also lead to a decent 
profit when we exit from these companies at the right time. We feel that the 
presence of the VCs, PEs and government funds are very important in any market.</font></p>
