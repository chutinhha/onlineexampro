        <p align="justify"><font face="Verdana" size="1"><b>Bahrain vies to be 
		Gulf’s finance hub<br>
		</b><br>
        <img border="0" src="gcc2.jpg" width="175" height="137" align="right" hspace="6" vspace="4">Bahrain has launched a US$1.5-billion financial development programme as 
		part of its attempts to reassert itself as the natural financial centre 
		of the oil-rich Gulf region, taking on competition from Dubai, Qatar and 
		Saudi Arabia. The opening of Bahrain Financial Harbour’s first phase, 
		offering 60,000 sq.m office space, coincides with the country’s central 
		bank’s efforts to fine-tune regulations to attract financial firms, 
		especially those focusing on Islamic banking, asset management and 
		insurance.<br>
		<br>
		The Kingdom, which took on the mantle of the regional financial hub from 
		Beirut in the 1970s, has in recent years watched a string of 
		international names moving to Dubai International Financial Centre and 
		Qatar Financial Centre.<br>
		<br>
		Saudi Arabia, the region’s largest oil-driven economy, is also courting 
		international finance, doubling the number of banks and licensing more 
		than 50 investment banks and brokerages in the past few years, 
		threatening Bahrain’s role as the country’s offshore banking hub. 
		Financial firms are investing in the Gulf to chase business associated 
		with increasing petrodollars that have seen the Gulf states’ foreign 
		reserves growing by about US$155 billlion a year over the past three 
		years, according to Saudi Arabia’s SAMBA Financial Group. With projects 
		worth US$1,000 billion planned in the region, banks are seeking 
		financing opportunities as the Gulf uses oil surpluses to build 
		infrastructure. New regulations encouraging alternative investment 
		firms, such as hedge funds, are expected to be passed in Bahrain within 
		days, says Rasheed Mohammed al-Maraj, Bahrain’s central bank governor. 
		The central bank has fostered the development of Islamic financial 
		products by leading the issuance of Islamic bonds and will soon 
		introduce new products to help the sector.<br>
		<br>
		Stephen Rothel, Bahrain Financial Harbour’s chief executive, said a 
		significant portion of the first building has received leasing 
		commitments from interested firms, including local institutions and 
		Dexia Asset Management of Luxembourg. Also interested is Bramdean, which 
		was considering setting up a regional base in Bahrain for asset 
		management services, said its chief executive Nicola Horlick.<br>
		<br>
		Jane Dellar, managing director of Bahrain Financial Services 
		Development, part of Bahrain’s Economic Development Board, says the 
		Kingdom is growing from a solid base, having already licensed 391 
		financial services firms, more than Dubai and Qatar.<br>
		<br>
		But competition from neighbouring Dubai is strong, with most main 
		investment banks opening their headquarters in the Dubai International 
		Financial Centre, using the tax-free zone as a launch pad for serving 
		the region. Qatar has developed a more inwardly focused centre, aiming 
		to create a domestic financial services sector to serve its rapidly 
		growing, gas-fuelled economy.<br>
		<br>
		A key target for the new development is Royal Bank of Scotland, which 
		has received a licence to operate in Bahrain but is also opening offices 
		in two other Gulf states, and has yet to define its regional base.</font></p>
