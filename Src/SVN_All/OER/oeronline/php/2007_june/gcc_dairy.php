		<p align="justify"><font face="Verdana" size="1"><b>Aramco, Dow sign 
		agreement for Ras Tanura plant<br>
		</b><br>
<img border="0" src="gcc1.jpg" align="left" hspace="8" style="border-style:solid; border-color:#EFEFE9; " vspace="4" width="175" height="161">Saudi Aramco and US’ Dow Chemical Co. have announced a deal to build a 
		petrochemical plant that industry insiders expect to be the largest 
		foreign investment in Saudi Arabia’s energy sector. The cost of the Ras 
		Tanura plastics and chemicals complex would be “mammoth”, said Saudi 
		Aramco’s President Abdallah Jumah, declining to give details. Aramco and 
		Dow signed a memorandum of understanding and will now enter a final 
		negotiation phase for the formation of a joint company to build, own and 
		operate the plant.<br>
        <br>
		Earlier, industry sources said the plant would have an investment cost 
		of at least US$20 billion. Soaring inflation has taken its toll of the 
		costs, which Aramco had last year estimated at around US$15 billion. Ras 
		Tanura will be one of the biggest plants of its kind, the two companies 
		said in a joint statement from Aramco’s headquarters in Dhahran.<br>
		<br>
		“When fully operational, the new complex will be one of the largest 
		grassroots plastics and chemicals production facilities in the world and 
		will be ideally positioned to serve major world markets,” the statement 
		said. The companies plan to float a 30 per cent stake in Ras Tanura and 
		raise the rest through debt, Jumah said. The investment is Dow’s largest 
		in the region and its biggest joint venture, said Chief Executive Andrew 
		Liveris. “We have other investments in the Middle East… but nothing 
		rivals the scale, size and breadth of this project. (It is) the largest 
		proposed joint venture in our 110-year history.”<br>
		<br>
		Dow is the largest US chemical maker by sales and has faced tough 
		competition from Middle East chemical producers, such as Saudi Arabia’s 
		Sabic, which have access to cheap crude and natural gas. Sabic is the 
		world’s largest chemicals company by market value. Sabic, along with 
		other companies, was invited to bid for Ras Tanura but was unsuccessful, 
		Jumah said.</font></p>
