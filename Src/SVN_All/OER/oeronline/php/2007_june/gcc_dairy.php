		<p align="justify"><font face="Verdana" size="1"><b>Clean energy firms 
		to get 100% ownership<br>
		</b><br>
		Abu Dhabi has launched the world’s first zero-carbon, zero-waste city. 
		Only companies engaged in renewable and clean energy, as well as science 
		and technology will be allowed to operate from this region, and will 
		enjoy 100 per cent foreign ownership rights and other attractive 
		incentives. “The attraction for businesses includes a one-stop-shop 
		programme of government services, transparent laws, tax-free 
		environment, intellectual property protection and proximity to nearby 
		manufacturers, suppliers and markets,” said Sultan Al Jaber, CEO of 
		Masdar, a subsidiary of Mubadala Development Company, the investment arm 
		of the Government of Abu Dhabi. The 6 sq. km energy, science and 
		technology community to be provided with solar energy will open in late 
		2009.<br>
		<br>
		The development is a unique, integrated ‘green community’ in the heart 
		of Abu Dhabi, which uses traditional planning principles of a walled 
		city, together with existing technologies to achieve a zero-carbon and 
		zero-waste sustainable development. The city will house the Masdar 
		Institute of Science and Technology, the graduate science and research 
		institute currently being established in cooperation with MIT; research 
		facilities; world-class laboratories; commercial space for 
		related-sector companies; light manufacturing facilities; and a 
		carefully selected pool of international tenants who will invest, 
		develop and commercialise advanced energy technologies. The city will 
		also host Masdar’s offices, residential space for employees, as well as 
		a science museum and edutainment facilities.<br>
		<br>
		Masdar CEO, Sultan Al Jaber said: “There is nothing like this in the 
		world. We are creating a synergetic environment; it is a true 
		alternative energy cluster, where you will find researchers, students, 
		scientists, business investment professionals, and policy makers all 
		within the same community.” He said the zero-carbon zero-waste city 
		would be a living example of sustainable development that would position 
		Abu Dhabi and Masdar at the forefront of intelligent resource 
		utilisation. “It will combine the talent, expertise and resources to 
		enable the technological breakthroughs necessary for truly sustainable 
		development.”<br>
		<br>
		The city will be car free, powered by renewable energy with services 
		digitally managed and providing real time information. With a maximum 
		distance of 200 m to the nearest transport link and amenities, the 
		compact network of streets will encourage walking. Shaded walkways and 
		narrow streets will create a pedestrian friendly environment in the 
		context of Abu Dhabi’s extreme climate. Surrounding land will contain 
		wind, photovoltaic farms, research fields and plantations, enabling the 
		city to be entirely self-sustaining.</font></p>
