		by 2020<br>
		</b><br>
        <img border="0" src="gcc6.jpg" width="175" height="160" align="left" hspace="6" vspace="4">By 2020, Qatar hopes to have zero-dependence on oil and gas revenues as 
		it moves to diversify its economy, according to its Minister of Finance 
		HE Yousuf Hussein Kamal. And by 2015, the country will have worked 
		towards its target as it plans to bring down dependence on the energy 
		sector to a mere 25 per cent, Kamal, who is also Acting Minister of 
		Economy and Commerce, said at the recently held Second Qatar Economic 
		Forum. The Minister remarked that the state was well aware that neither 
		oil nor gas was an infinite resource and no one can tell where prices of 
		these commodities would stand in the next 10 to 15 years. <br>
		<br>
		“Ten years ago, we were in a crisis. External debts were 148 per cent of 
		our GDP. We had to accomplish financial restructuring and economic 
		reform. This was extremely painful and unfortunately, the pain of reform 
		reaches the lower income group. But the pain is also with those who made 
		the decision,” he said. Such ‘painful’ steps included cutting down on 
		recruitment in the government sectors and also decreasing investments 
		out of the state budget.” Since 2003, “Qatar is getting the fruits of 
		the decisions taken in those years,” he stated. <br>
		<br>
		Kamal admitted to some shortcomings which, he said, would be dealt with 
		through various acts of legislation. “There is a gradual openness and we 
		should get the private sector to accept this. There will be 
		liberalisation in certain sectors by 2013. Our financial systems and 
		legal systems are not hand-in-hand with international levels. We are now 
		unifying these and getting legislation ready for global acceptability.”
