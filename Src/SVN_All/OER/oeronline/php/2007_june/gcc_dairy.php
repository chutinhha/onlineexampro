		<p align="justify"><font face="Verdana" size="1"><b>Kuwait ends dollar 
		peg<br>
		</b><br>
        <img border="0" src="gcc4.jpg" width="190" height="88" align="right" hspace="6" vspace="4">Ending months of speculation, Kuwait has abandoned its four-year old 
		currency peg to the US dollar in a move that is expected to trigger 
		renewed calls from monetary experts in the UAE and other GCC countries, 
		reeling under imported inflation resulting from a tumbling greenback, to 
		follow suit.<br>
		<br>
		The Kuwaiti Central Bank’s move to switch the dinar exchange rate 
		mechanism to a basket of currencies, although aimed at softening its 
		spiralling domestic inflation, is feared to further delay the GCC 
		monetary union which is already poised to miss the 2010 deadline, 
		analysts say.<br>
		<br>
		Kuwait, the third-largest Arab oil producer, ditched the dinar’s peg 
		against the dollar in favour of a basket of currencies, which it 
		abandoned in 2003 to prepare for the GCC monetary union. While the move 
		was long expected to stave off the negative impact of a declining 
		dollar, it caught markets and fellow central bankers unawares.<br>
		<br>
		An economist said other countries in the region which are suffering from 
		their alliance to a sliding dollar should start moving in the same 
		direction. Shaikh Sultan bin Saud Al Qassimi, Chairman of Capital 
		Industries Investment and Barjeel-Geojit Securities described the Kuwait 
		Central Bank move as wonderful. “It is time the UAE start switching its 
		currency peg away from the dollar to a basket of currencies. We have 
		been undecided about this for the past two years. As the dollar is 
		expected to continue its slide for at least two years, we should move 
		without further delay,” he said.<br>
		<br>
		Kuwait’s surprise move came in the wake of International Monetary Fund 
		ruling out such a possibility until 2010. Earlier, Mohsin Khan, IMF 
		director for the Middle East and Central Asia, reported as saying that a 
		depegging from dollar was not good for the region’s financial stability. 
		“We believe that it is inevitable for Gulf currencies to stay with the 
		dollar peg until the currency union, which I have no reason to think not 
		possible by 2010,” he said. “Gulf states should not alter their currency 
		pegs, which will do little to curb inflation and might threaten 
		perceptions of Gulf currencies as stable,” he has said.<br>
		<br>
		However, Kuwait’s central bank governor said his country was still 
		committed to monetary union and was only acting in the “national 
		interest” to contain inflation. “Until the completion of all the 
		requirements to achieve the currency union and the launch of the Gulf 
		currency, the Central Bank of Kuwait will continue to adopt the basket 
		system.” If the currency basket mirrored the sources of Kuwait’s top 
		merchandise imports, it would roughly consist of 46 percent dollars, 29 
		percent euros, 15 percent yen and 10 percent sterling.</font></p>
