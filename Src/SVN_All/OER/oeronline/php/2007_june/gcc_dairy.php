		<br>
		<b>The trade surplus of GCC countries is forecast to hit US$585 billion 
		and the GDP to touch US$750 billion indicating the rising level of 
		economic activity in the region</b><br>
		<br>
<img border="0" src="gcc_dairy.jpg" align="left" hspace="8" style="border: 2px solid #EFEFE9; " vspace="4" width="300" height="141">The 
		combined current account surpluses of the GCC countries is projected to 
		surge to US$585 billion in the 2005-2007 period as a result of higher 
		export earnings. According to SAMBA Financial Group of Saudi Arabia, the 
		new record GCC surplus would be higher than that of the next two 
		economies – China with a projected US$521-billion surplus and Japan with 
		a projected US$437-billion surplus.<br>
		<br>
		Data from the Institute of International Finance (IIF) show a nearly 
		10-fold increase in GCC current account surpluses from 2002 through 
		2006. The IIF’s estimate of last year’s GCC current account surplus of 
		US$227-billion compares with the IMF’s estimated US$571-billion current 
		account surplus for all oil exporters, including Russia, Norway and 
		Mexico.<br>
		<br>
		IIF said that on the back of this record surplus, the foreign assets of 
		the GCC had risen since the beginning of this decade by US$400 billion, 
		and now surpass US$1 trillion, with the lion’s share being held by state 
		investment funds. “There are estimates that already for some of the 
		smaller GCC members’, earnings from investments are on par with or may 
		even exceed earnings from hydrocarbons.”<br>
		<br>
		According to IIF estimates, from the beginning of 2006 through 2007, the 
		GCC countries would purchase more than US$450 billion in foreign assets, 
		complementing the inward bias of overall investment expenditures. “Given 
		the already robust official foreign assets of the oil producers, with 
		their individual capital accounts in surplus, this is not surprising. 
		With little information available, it is difficult to pinpoint the size 
		of the international portfolios of government agencies, let alone their 
		rate of growth.”<br>
		<br>
		In its forecast, SAMBA said Saudi Arabia, after recording its eighth 
		consecutive current account surplus in 2006 – a record US$95.5 billion – 
		would, however, see a drop this year. The bank said imports would grow 
		at a similar rate to last year, but oil export revenues would decline 
		due to a softer oil market. “As a result, merchandise imports will equal 
		about one-half, rather than one-third, of the value of exports. With 
		transfers, such as worker remittances, and trade in services taken into 
		account, we forecast a current account surplus for 2007 of US$58 
		billion, 39 per cent lower than the 2006 surplus. Even the International 
		Monetary Fund has said the Gulf oil-producing economies were expected to 
		grow at a slightly slower pace, with their gross domestic product 
		surging to $749.6 billion while recording a decline in surplus in 2007 
		as oil prices and output drop. In its Regional Economic Outlook, the IMF 
		said that region would, however, remain resilient to shocks. Mohsin Khan 
		of the IMF’s Middle East and Central Asia department, said only Saudi 
		Arabia – the largest economy in the Middle East and Central Asia – was 
		expected to register a slightly higher real growth of 4.8 per cent, 
		compared to 4.6 per cent in 2006. The GDPs of the UAE and Qatar would 
		see a real growth of 8.2 per cent and 8 per cent, respectively, compared 
		to 9.7 per cent and 8.8 per cent, respectively, in 2006. Kuwait’s GDP 
		growth would drop from 5 per cent in 2006 to 3.5 per cent while 
		Bahrain’s economy would drop from 7.7 per cent in 2006 to 6.9 per cent. 
		Oman’s GDP would grow at almost the same pace as last year, increasing 
		from 5.9 per cent to 6 per cent in 2007.<br>
		<br>
		Khan said in value terms all GCC economies, except Kuwait, would 
		register a higher GDP. The Kuwaiti economy would see a drop from US$96.1 
		billion in 2006 to US$95.4 billion. The size of the Saudi economy was 
		expected to reach US$354.9 billion, compared to US$348.6 billion in 2006 
		while the UAE’s GDP would reach US$186.2 billion compared to US$168.5 
		billion the previous year. Qatar would also see its economy expanding to 
		US$57.3 billion from US$52.7 billion registered in 2006. Bahrain and 
		Oman-the only non-OPEC members in the GCC – were expected to see their 
		economies growing to US$17.5 billion and US$38.3 billion, respectively, 
		compared to US$16.1 billion and US$36 billion in 2006. In total, the GDP 
		in the GCC would grow nominally from US$718 billion in 2006 to US$749.6 
		billion in 2007.<br>
		<br>
		The IMF official said that non-oil GDP continues to grow in Gulf 
		countries, pointing out that it should be factored in while assessing 
		their economic performance. “Non-oil GDP has been growing very rapidly. 
		It will still be growing despite the drop in oil-GDP growth. It grew 
		notably by 11.1 per cent in 2006 in the UAE and was expected to grow by 
		10.1 per cent in 2007, while in Saudi Arabia it is expected to grow 6.9 
		per cent in 2007, compared to 7.3 per cent in 2006.”</font></p>
