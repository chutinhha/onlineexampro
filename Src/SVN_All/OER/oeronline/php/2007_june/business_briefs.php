</font><font face="Verdana" size="2" color="#CC3300"><b>Bank Sohar starts 
operations</b></font><font face="Verdana" size="1"><br>
<img border="0" src="Bank_Sohar.jpg" hspace="8" style="border: 1px solid #CCCCCC" align="right">The sixth licensed bank in Oman – Bank Sohar – finally started operations during 
the first week of May. Significantly, a new bank has come in the Sultanate after 
a gap of 12 years. The bank also scored another first by going public even 
before starting operations. “This is the first time in the history of Omani 
capital markets that a company had been allowed to raise funds from the public 
without having any track record. The idea behind this move was that there is 
enough potential for a bank to grow in this market. With the government lining 
up billions of dollars for developing world-class infrastructure in the country, 
the new bank has its job cut out. But it makes for an interesting analysis once 
the bank announces its first quarterly results,” says an analyst.<br>
<br>
Hilal bin Hamoud bin Hamad Al Ma’amari, chairman of Bank Sohar said: “The idea 
behind establishing Bank Sohar came under the blessings of His Majesty Sultan 
Qaboos bin Said, and in response to the economic developments that Oman has been 
witnessing in the last few years. The policies of the government encourage 
domestic and foreign investments. A number of government development projects 
coming up across the country indicate that the economy is on the growth path. 
Bank Sohar shoulders the responsibility of implementing best banking policies in 
Oman. We are committed to implementing international standards in banking.” Bank 
Sohar CEO Nani Javeri said: “The presence of Bank Sohar will be felt by all as 
we will have a presence in all major Omani cities and towns. Our head office is 
physically located in Muscat, but our people, technology, products and services 
will be available all over the country. The bank has already achieved 80 per 
cent Omanisation.”<br>
<br>
Meanwhile, the bank launched a series of prizes worth over half a million rials. 
The monthly prizes include one RO20,000 cash prize, one Lexus car, five prizes 
of shopping vouchers for a year and five prizes of fuel vouchers for a year. 
Customers maintaining RO100 balance in ‘Al Mumayaz’ savings account will get two 
chances to win prizes in the draw (every month) conducted during the middle and 
beginning of every month. The bank is also offering a cash prize of RO100,000 in 
the mega draw during the year-end period. Chances of winning the prizes are high 
for children and the elderly as the bank offers two chances for every RO100 
balance in the savings account, four chances for every RO100 balance in the 
children savings account.</font><p align="center" style="margin: 3px 10px">
