<font face="Verdana" size="2" color="#CC3300"><b>New firm to market Omani oil 
products<br>
</b></font><font face="Verdana" size="1">Oman oil products are all set to get a 
new marketing thrust on a global scale. A new company, Oman Trading 
International Limited Company (OTILC), has been set up to shoulder marketing and 
trading of Oman oil products and petrochemicals in the world market. The company 
has been formed under the auspices of Ahmed bin Abdulnabi Macki, Minister of 
National Economy and Deputy Chairman of the Financial Affairs and Energy 
Resources Council. OTILC was established in January 2006, pursuant to an 
agreement on a joint venture between Oman Oil Company (holding 51 per cent 
stake) and Vetol International Group Company (with 49 per cent). Speaking at the 
launch ceremony, organised in the presence of Sheikh Ahmed bin Said Al Maktoum, 
chairman of the Civil Aviation Department in the Emirate of Dubai, who is also 
chairman of Emirates Air, Macki said the Sultanate’s government had not only 
added value to Oman’s oil and LNG products, refinery byproducts and other 
investment projects at Sohar Industrial Port but also provided Omani youths with 
job opportunities. Oman Oil Company had mega investments in European, Asian and 
American countries in the field of oil, sea transport, petrochemical, aromatics 
and methanol projects, he said, adding that all were joint projects with 
internationally reputed companies.<br>
<br>
Meanwhile, Oman Shipping Company (OSC) signed an MoU with Oman Trading 
International Limited Company (OTI) and Vetol International Group Company on the 
establishment of a joint company for transporting methanol products from Salalah 
Methanol Company. The agreement was signed on behalf of OSC by Ahmed bin 
Abdulnabi Macki, for OTI by its board chairman, and on behalf of Vetol by its 
the chairman and CEO. OSC has 80 per cent in the proposed company while Vetol 
has 20 per cent shares.<br>
