</font><font face="Verdana" size="2" color="#CC3300"><b>Oman Cables leads in 
Awards for Excellence<br>
</b></font><font face="Verdana" size="1">The prestigious ‘Flame of Excellence 
Award’ and the ‘Exporter of the Year’ awards for the year 2006 have been bagged 
by Oman Cables Industry. The ninth edition of Oman Awards for Excellence also 
saw Al Hassan Engineering, Matrah Cold Stores, Midwest Oilfield Services and Sun 
Packaging fighting in the final for the ‘Flame of Excellence’ award. National 
Aluminium Products and Sun Packaging Company were the other finalists for the 
‘Exporter of the Year’ award, sponsored by National Bank of Oman. <br>
<br>
Zahara Tours won the Tourism Award, sponsored by Omantel. Bahwan Tours and 
Shangri-La’s Barr Al Jissah Resort &amp; Spa were the other finalists for the 
‘Tourism Award’. Oman Refinery Company won the ‘Environmental’ award, sponsored 
by Oman Wastewater Services. The other finalist for the award was The Wave, 
Muscat. Pizza Hut received the ‘People Development’ award. Gulf Air and National 
Hospitality Institute were the other finalists for the award. Mohammed Jahwari 
of Midwest Oilfield Service LLC received the ‘Entrepreneur of the Year’ award, 
sponsored by MB Holding Company. Hani Mirza of Bin Mirza International LLC and 
Mohammed Al Busaidi of Muscat Sports were the other finalists.<br>
<br>
The Oman Awards for Excellence is an annual award programme developed by the 
private sector in Oman to celebrate the accomplishments of individuals, 
companies and other contributors who are creating jobs and driving the Omani 
economy. The panel of judges comprised Tony Kinsman, CEO, Sohar Aluminium; Nasir 
bin Issa Al Ismaily, general manager, ECGA; Nani Javeri, CEO, Bank Sohar; 
Abdulaziz Al Balushi, CEO, Alliance Housing Bank; and Lujaina Mohsin Darwish, 
joint deputy managing director, MHD.<br>
