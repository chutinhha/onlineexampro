a US$146-million term loan refinancing for Corporation’s joint venture project, 
Barr Al Jissah Resort Co, with the government of the Sultanate of Oman. The 
group has many large deals in the pipeline and the current US$100-million deal 
is considered to be a win-win transaction both for the Corporation and the 
participating banks.<br>
