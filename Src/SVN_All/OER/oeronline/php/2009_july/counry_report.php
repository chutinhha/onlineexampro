                <b>
                <font face="Verdana" size="1">
                The Rise and Rise of Turkey</font></b><p align="justify" style="margin: 3px 10px" dir="ltr">
                <font face="Verdana" size="1">
                The Eurasian state is making all efforts to minimiSe the impact 
                of global economic slowdown on its economy. AKSHAY BHATNAGAR 
                REPORTS FROM TURKEY<br>
                <br>
                The moment one gets down at the Istanbul Ataturk International 
                Airport one can feel the vibrancy in the environment. Scores of 
                people are rushing in to catch their flight and many more are 
                exiting from the airport to move on to their desired 
                destinations in Turkey. With the airport brimming with so much 
                of action, it indicates that the claims of the Turkish 
                government that the country on the crossroads between the 
                continents of Europe, Asia and Africa is successfully managing 
                the challenge of global recession are legitimate. Don’t believe 
                it yet? According to The Economist magazine, foreign investors 
                remain highly optimistic about Turkey’s stance during the global 
                financial turmoil. Many economic developments taken place during 
                the last few weeks confirms The Economist’s view point.<br>
                <br>
                <b>The Pull Factor<br>
                </b>Zaman-Ford Otosan, a joint venture between US automotive 
                giant Ford and Turkey’s largest industrial conglomerate, Koc 
                Holding, has just started exports to the US. Ford has never 
                before exported cars to its home country from a Ford factory in 
                Europe, and Turkey will be the first European country to export 
                Ford cars to the “homeland”. The Turkish automobile manufacturer 
                Tofas, a joint venture between Italy’s Fiat and Koc Holding, 
                will invest around EUR 300mn this year in its new research and 
                development (R&amp;D) activities, as well as new products. Turkey is 
                continuously boosting its power in the automotive sector. The 
                country, exporting automobiles to more than 100 countries all 
                over the world, also supplies crucial spare parts for many 
                countries. Sarigozlu Hidrolik Makine ve Kalip Sanayi, a company 
                operating in the Aegean province of Manisa, is manufacturing 
                castings for ten big brands, including Porsche and Jaguar.<br>
                <br>
                The US private equity Global Environment Fund (GEF) is eyeing 
                Turkey’s growing alternative energy sector. GEF, managing a 
                total of $1bn worth of funds globally, aims to widen its 
                investments in renewable and alternative means of energy. 
                Another notable addition to energy investments in Turkey comes 
                from Edison SpA (EDN.MI), a leading Italian energy company. The 
                company plans to invest EUR 1bn for expansion in the 
                southeastern Mediterranean area. In yet another development, an 
                energy partnership between the German company EnBW and Turkish 
                conglomerate Borusan Holding has been signed to invest EUR 2.5bn 
                over the next 8-10 years. The joint venture will focus primarily 
                on renewable energy and make an investment to produce 1,000 
                megawatts (MW) of power after completing 17 projects, with a 
                final target of 2,000 MW.<br>
                <br>
                The Turkish pharmaceutical market, with an annual volume of 
                $10bn, has drawn the attention of Japanese investors, in 
                addition to European and US companies. Japan’s two largest 
                pharmaceutical companies have already started to operate in 
                Turkey. Ken Jones, EMRA CEO of Astellas (one of the two Japanese 
                pharma giants), said, “Turkey has the sixth largest 
                pharmaceutical market of Europe, and it will rank in the top ten 
                list of the world soon”. One of world’s largest electronics 
                retailers, Best Buy, is opening stores in Turkey. Robert A. 
                Willet, CEO of Best Buy, said they plan to open three stores in 
                Izmir, Ankara and Bursa by the first quarter of 2010. Willet 
                stated that there is a fast growing market in Turkey, which he 
                sees as the “China of Europe”.<br>
                <br>
                In another boost to the local industry, Raytheon (a major 
                American defense contractor and industrial corporation with core 
                manufacturing concentrations in defense systems and defense and 
                commercial electronics) has appointed Roketsan, a Turkish 
                missile manufacturer, as an international supplier of a key 
                component of the Patriot Guidance Enhanced Missile-Tactical, or 
                Patriot GEM-T. Roketsan is a proven leader in missile and rocket 
                programmes and has been a major part of Turkey’s defense 
                industry since 1988.<br>
                <br>
                Boeing, that provides direct employment for more than one 
                thousand people in Turkey, expects to reach a business volume of 
                more than $1.2bn by 2013 in the country. Joe McAndrew, Vice 
                President-Europe, Boeing Business Development, recently stated 
                that a big portion of their investments will be located in 
                Istanbul, Ankara and Izmir and added that the KC-135R tanker 
                aircraft project in Turkey stands out as a ‘model’ for local 
                studies.<br>
                <br>
                China is also not left behind in making inroads into Turkey. 
                Jinan province of China is making a $200mn investment for health 
                tourism in Turkey. Steve Ballmer, CEO of the world’s largest 
                software company Microsoft, recently laid the foundation of an 
                ‘Innovation Center’ in Ankara. He stated that Turkey stands to 
                be one of the top ten countries for Microsoft in terms of 
                business volume. Ballmer said Turkey, with its dynamic economy 
                and its young, technically-inclined population, has a vast 
                potential for development and added that the country may become 
                the software center of Europe.<br>
                <br>
                Overall, Turkey has attracted significant amount of FDI so far. 
                Even in 2008, gross FDI inflow to Turkey was around $17.7bn, an 
                impressive figure despite the challenging environment. This 
                year, the country is expecting an FDI inflow of over $10bn. The 
                country is fairly stable on the foreign exchange reserves front 
                as well. Towards the end of April this year, the Central Bank of 
                the Republic of Turkey’s reserve was pegged at $62.4bn. <br>
                <br>
                <b>Countering Economic Slowdown<br>
                </b>As Turkey is intrinsically integrated into the global 
                economy, the financial meltdown has casted an impact on the 
                country but it is not as severe as seen in many of the western 
                economies. The Turkish government has been prudent in taking 
                adequate measures to withstand the crisis. “As part of our 
                monetary measures, the rates have been cut five consecutive 
                times since November. In totality, the central bank has cut the 
                borrowing rate by 625 basis points to 10.5%; and its lending 
                rate by 675 basis points to 13%. We expect the inflation to come 
                down to 7.5% towards the end of the year,” said HE Engin Turker, 
                Turkey’s ambassador to Oman.<br>
                <br>
                Among the prominent measures taken to boost investor confidence 
                and financing, the government has permitted payment of overdue 
                taxes in installments. Last November, a new law was approved 
                that facilitates bringing certain assets such as gold, cash and 
                securities, in Turkey and/or registering of such assets. The 
                profits earned outside the country are exempted from income and 
                corporate taxes if they are brought to Turkey in certain 
                conditions. The Council of Ministers has also been empowered to 
                increase and expand the coverage of government guarantee on 
                deposits for two years. The 10 per cent withholding tax levied 
                on sale and purchase of the shares by domestic investors was 
                withdrawn.<br>
                <br>
                To support exports, production and employment, the central bank 
                has increased the export rediscount credit limit from $500mn to 
                $1bn. To prop up small and medium enterprises (SMEs), the Small 
                and Medium Sized Industry Development Organization (KOSGEB) is 
                giving interest free loans up to Turkish Lira 700mn 
                (approximately RO175mn) to SMEs. Another loan of upto $650mn 
                with interest free rate has been allocated to export oriented 
                SMEs. In 2009, KOSGEB is expected to provide $1bn in loans to 
                SME exporters. This year, Turkey’s Eximbank plans to provide 
                $12.9bn in financial support to exporters. To increase the 
                resilience of the financial markets, apart from other measures, 
                the foreign exchange reserve requirement has been reduced from 
                11% to 9%. With this move alone, $2.5bn have been injected into 
                the banking system. The central bank has also given the 
                assurance that if required the foreign exchange reserve 
                requirement ratio may be reduced further.<br>
                <br>
                “Additional measures have been taken to support the reverse 
                dollarization process and promote deposits and loans in Turkish 
                Lira,” added HE Turker. To improve the efficient functioning of 
                the interbank foreign exchange market, the central bank has 
                extended the maturity of its US dollar and Euro lending to banks 
                in the foreign exchange deposit markets from one month to three 
                months. The lending rates for transactions have been reduced 
                from 7% to 5.5% for US dollar and from 9% to 6.5% for Euro. The 
                foreign exchange sale auctions in March have also added to 
                foreign exchange liquidity in the market.<br>
                <br>
                All these steps are expected to give a fillip to the economy in 
                Turkey. The global financial management and advisory company 
                Merrill Lynch has estimated a 3.2 percent economic growth rate 
                in Turkey for 2010, despite the general slowdown in the world 
                economy. Turkey will make a quicker recovery than other 
                countries of the region thanks to relatively minimal damage in 
                the banking sector, households and firms, according to a Merrill 
                Lynch analysis.<br>
