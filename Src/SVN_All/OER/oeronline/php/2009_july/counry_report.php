                      T</b></font><font face="Verdana" size="1">urkey has 
                      preserved the legacy of its past while working towards a 
                      modern nation, and as a result has become a destination by 
                      choice for tourists from all over the world. Due to its 
                      pleasant weather, natural beauty, rich history, 
                      well-developed infrastructure and outstanding hospitality, 
                      Turkey is preferred by the travelers from the Middle East.
                      <br>
                      <br>
                      Lonely Planet has described Turkey as “Landscape dotted 
                      with battlegrounds, ruined castles and the palaces of 
                      great empires. This is the land where Alexander the Great 
                      slashed the Gordion Knot, where Achilles battled the 
                      Trojans in Homer’s Iliad, and where the Ottoman Empire 
                      fought battles that would shape the world. History buffs 
                      can immerse themselves in marvels and mementos stretching 
                      back to the dawn of civilisation. Then again, if you want 
                      to simply unwind, spend an afternoon being pampered at a 
                      hamam, or let the warm waters off the Mediterranean coast 
                      lap at your toes. Adventure lovers can head east to Nemrut 
                      Dagi National Park. Bon vivants need look no further than 
                      Istanbul, where the markets and bars are among the most 
                      stylish and atmospheric, and the mod Ottoman cuisine rates 
                      as the tastiest, in the world.”<br>
                      Most visits to Turkey begin with Istanbul, the capital 
                      city of both the Byzantine and Ottoman empires. The city 
                      still retains impressive reminders of both as you take a 
                      round of it. It lies where two continents (Europe and 
                      Asia) and two seas (Black Sea and Sea of Marmara) meet. 
                      Its main artery is the Bosphorus. In the city of minarets, 
                      at every step you will be amazed by its natural beauty and 
                      historical richness.<br>
                      <br>
                      From Istanbul you can go to Princess Islands. The 
                      beautiful wooden houses and the wonderful views of the 
                      cluster of nine islands leaves an unforgettable impression 
                      on visitors. These islands, on which monasteries were 
                      built during the Byzantine period, are almost an hour away 
                      from the city. Enjoy looking at the old churches, mosques 
                      besides the summer homes of the rich and famous sitting in 
                      your horse-drawn carriages<br>
                      <br>
                      After Istanbul, you may continue your journey eastwards 
                      into the heart of Anatolia to the capital Ankara, or along 
                      the Black Sea coast, or southward along the Aegean coast 
                      to the Mediterranean. If you follow the Sea of Marmara 
                      eastward on your journey from Istanbul, the highway which 
                      circles it will bring you to Bursa. Alternatively you can 
                      make this journey in part by a ferry boat to Yalova on the 
                      southern Marmara shore and then complete the 60km to Bursa 
                      passing through the famous peach orchards of the province. 
                      It was here that first capital of the Ottoman Empire was 
                      founded by Orhan Gazi in 1326, and it is here too that 
                      amidst lovely natural surroundings you can see the first 
                      examples of typical Ottoman architectural style.<br>
                      <br>
                      Known as Green Bursa, the city is filled with gardens and 
                      parks. The place is famous for silk trade and thermal 
                      springs. Thirty-six kilometers from Bursa is Uludag, the 
                      largest center for winter sports in Turkey. It offers a 
                      variety of activities, accommodation and entertainment. 
                      The slopes are easily reached by car or cable car. 
                      December to May is the best time for skiing, although 
                      Uludag National Park is worth a visit at any time of the 
                      year for the lovely views (snowcapped mountains) and fresh 
                      air. Don’t forget to carry your woollens as the place is 
                      generally cold. On the Aegean coast, which can be reached 
                      from Bursa, you find at almost every step the remains of 
                      ancient civilizations from various periods of Anatolian 
                      history.<br>
                      <br>
                      Close to the entrance of the Canakkale Bogazi (the 
                      Dardanelles), in the province of Canakkale, is Homer’s 
                      Troy (Troia), where excavations have brought to light the 
                      riches of nine different settlements, one on top of 
                      another. The inhabitants of these nine settlements show a 
                      high level of civilization that stretches back to 3000 BC. 
                      Continue your journey through the wonderful holiday towns 
                      of Akcay, Oren, Ayvalik and Foca and you arrive at the 
                      ‘pearl’ of the Aegean, Izmir. Such is the beauty of the 
                      area, and the totally relaxing atmosphere, that the 
                      holidaymaker abandons all sense of time. Just 80 km of 
                      Izmir is Cesme, famous for its thermal waters and 
                      sparkling clear sea. In this rich region you can visit the 
                      famous ‘Arcadian Way’ in Ephesus, lined with elegant, 
                      marble columns. Nearby is the small house where the Virgin 
                      Mary is reputed to have spent her last days. Not far from 
                      both is the popular resort of Kusadasi or ‘Bird Island’, 
                      where you can rest awhile by the beautiful turquoise 
                      waters of the Aegean. From Kusadasi you can turn inland 
                      towards Denizli, which is near Pamukkale (Cotton Castle). 
                      Here, thermal spring waters laden with calcium have run 
                      off the plateau’s edge, creating a cascade of petrified 
                      basins - an extraordinary and marvelous natural work of 
                      art. The waters have been popular since Roman times for 
                      their therapeutic powers, and just behind the basins are 
                      the ruins of ancient Hierapolis.<br>
                      <br>
                      Backtracking to rejoin the Aegean road that continues 
                      southwards, we find the great Oinian cities of Priene, 
                      Miletus and Didyma, which witnessed some of the great 
                      advances of human culture. Continuing south to the Gulf of 
                      Gokova, we come to the lively and popular seaside resort 
                      of Bodrum, known in ancient times as Halicarnassus, 
                      dominated by a castle built by the Knights of Rhodes. 
                      Marmaris, further to the south, can easily be reached by 
                      road but if you really want to savour the full pleasure of 
                      this beautiful coastline, take a ‘Blue Voyage’ yacht tour. 
                      Marmaris is a tourist’s paradise set in a deep fjord-like 
                      inlet where the pine forests come right down to the 
                      shoreline of the beautiful bay lined with comfortable 
                      hotels and small guest-houses serving delicious seafood. 
                      Its fully-equipped yacht marina is able to accommodate 
                      private yachts and there are numerous yachts for charter. 
                      Endless blue sea bathed in sunshine, orange groves, banana 
                      plantations, waterfalls, forests of palm and pine that 
                      splash the foothills of the Toros mountains in a rainbow 
                      of colours with sandy beaches stretching mile after mile… 
                      this is Antalya, the tourism capital of Turkey. For those 
                      with keen interest in history, the city offers ancient 
                      theatres of Aspendos, Termessos and Side, to the many 
                      works and monuments that date back to Seljuk times. The 
                      place is very popular among tourists for its theme based 
                      deluxe resorts. In Antalya, you must visit the Mardan 
                      Palace, the most expensive hotel in entire Europe. Leaving 
                      the sunny Mediterranean behind and cutting inland into the 
                      heart of Anatolia, one arrives at the capital of modern 
                      Turkey, Ankara. The city offers the Museum of Anatolian 
                      Civilizations where relics of the world’s oldest cultures 
                      are displayed. Moving on, Konya is the home to the mystic 
                      poet and philosopher Mevlana who founded the sect of 
                      Mevlevis known to the west as the ‘Whirling Dervishes’.<br>
                      <br>
                      In the mountains of Adiyaman is Mount Nemrut, with a 
                      height of almost 2,150m, on whose peak the spectacular, 
                      toppled statues of gods lie. These were erected 2,000 
                      years ago for the Commagene King Antiochus I. Istanbul is 
                      likely to be the place where you will come back to take 
                      the flight back home. Make sure you haven’t missed out on 
                      your boat trips along the Bosphorus to enjoy the 
                      spectacular night view of Istanbul! In all likelihood 
                      while leaving Turkey you will contemplate making a 
                      comeback to the country to cover the areas that you 
                      couldn’t make it as there is so much to <br>
