              <p align="justify" style="margin-left: 10px; margin-right: 10px"><font size="1" face="Verdana, Arial, Helvetica, sans-serif"> 
                <br>
                </font><b><font face="Verdana" size="4">Survival of the Fittest</font></b><br>
                <b><font size="1" face="Verdana, Arial, Helvetica, sans-serif">Having 
                been pushed to a corner by the global financial crisis, companies 
                in Oman are fighting back with novel ideas, inventive solutions 
                and creative thinking. Mayank Singh reports</font></b></p>
              <p align="justify" style="margin-left: 10px; margin-right: 10px"> 
                <font size="1" face="Verdana"> <img src="images/cover01.jpg" hspace="9" vspace="3" border="0" align="right">Hussain 
                Ahmed Ghuloom, CEO, Al-Waqia Shoes Company, sits in his office 
                with a fractured ankle. The mishap which took place during a game 
                of squash means that his feet is going to be plastered for the 
                next two months. Ironically, a broken ankle is the least of his 
                worries, as Ghuloom grapples with bigger challenges facing his 
                business. “Our sales are down by almost 35 per cent, our exports 
                by 40 per cent and local sales have gone down by 26-27 per cent,” 
                he says stoically. Europe, one of Al-Waqia’s biggest markets is 
                the worst affected. Spain, where the company sold 9,000 pairs 
                of shoes in 2008 and 28,000 pairs in 2007, has completely dried 
                up. The UK, Poland and Switzerland markets have stagnated. In 
                the GCC things are no better with sales in Saudi Arabia, the biggest 
                economy in the region plummeting by 75 per cent, Dubai and Qatar 
                are no better.<br>
                <br>
                Al-Waqia shoes is not the only company that has been impacted 
                adversely by the global economic meltdown – Technique LLC, a multi-product 
                trading, engineering and service company has seen a 16 per cent 
                drop in trade volumes during the first quarter of 2009. Says Nick 
                Moulton Thomas, general manager of the company, “We have a large 
                industrial sales division which deals with paints, lubricants, 
                marine paints and fire resistant paints. Since many projects are 
                going slow this is forcing people to buy less paint or to postpone 
                their decision to repaint.”<br>
                <br>
                <strong>Go slow mode </strong><br>
                <img src="images/cover04.jpg" width="300" height="421" hspace="9" vspace="3" border="0" align="left"> 
                The slowdown of projects is also forcing companies to go back 
                to the drawing board. National Gas which supplies LPG, has not 
                been affected in its primary business as the consumption of domestic 
                gas has remained unaffected, but its engineering of gas systems 
                business which comes under Innovative Energy Systems (Inersys) 
                a joint venture company between National Gas owned Shoulat al 
                Aman and Ashtech Gas Engineering &amp; Services of UAE is facing 
                the brunt of the slowdown. Says Goutam Sen, general manager, National 
                Gas, “Engineering of gas systems is linked to projects and so 
                its performance has been impacted.”<br>
                <br>
                Three major projects that the company was looking forward to have 
                been delayed for one reason or the other. On one project though 
                the company got a letter of intent four months back, it is still 
                awaiting an advance to start construction. Another project that 
                the company won has not taken off, but mercifully the company 
                has not committed any resources to the venture. “The vibe that 
                we get is that most people are going slow,” says Sen.<br>
                <br>
                A’Saffa Poultry Farms which completed a major capacity expansion 
                in February 2009, got caught on the wrong foot due to the slowdown. 
                The company’s production went up from 31,000 chickens a day (12mn 
                chickens a year) to 52,000 chickens a day (16mn chickens a year), 
                post expansion. The initiative cost the company RO9mn and was 
                was partly funded by doubling the company’s equity base from RO5mn 
                to RO10mn. “Unfortunately our expansion came on stream right in 
                the middle of the economic downturn in the months of March, April 
                and May. Due to this the purchasing power of people got reduced 
                by 20-25 per cent,” says Dr Nasser al Mauly, CEO, A’Saffa. This 
                affected the off-take of its products in supermarkets and hypermarkets, 
                forcing the company to look for newer markets to absorb the extra 
                capacity.<br>
                <br>
                <strong>Hitting cash flows</strong><br>
                <img src="images/cover05.jpg" hspace="9" vspace="3" border="0" align="right">The 
                reduced off take in hypermarkets is corroborated by others like 
                National Mineral Water Company (NMWC). Says Pankaj Chugh, its 
                general manager, “Sales in hypermarkets are down by 30-35 per 
                cent. Though most of this erosion comes from non-essential items 
                like garments and electronics, this indirectly has an adverse 
                impact on us as our payments get delayed.” As a result, the payments 
                which NMWC used to get within 90 days has now been stretched to 
                120 days. The choking up of inter-company cash flows is creating 
                an artificial liquidity crisis <br>
                in the market.<br>
                <br>
                The malaise is not just restricted to Oman, The National Detergent 
                Company (NDC) has encountered similar issues in Saudi Arabia. 
                Though the company extended the payment window from 90 to 120, 
                it was still finding it difficult to recover its payments. Says 
                V Sundaresan, director and general manager, NDC, “We have become 
                strict on delivery, if a party does not pay on time, we keep the 
                subsequent orders on hold.”<br>
                <br>
                A number of small and medium sized companies operate as subcontractors 
                for larger firms, so if the bigger company faces a problem, it 
                automatically filters down to the SME. Smaller players do not 
                have strong cash flows as large companies, so even a few delayed 
                payments can prove to be a fatal blow for them. The worst of the 
                lot are SME’s who are entirely dependent on a single company for 
                business. Abdulnasir al Raisi, head, SME credit and marketing 
                department, BankMuscat explains the spiral effect, “It is a matter 
                of psyche, people have become scared of moving their money – there 
                is a feeling that if one pays others, they may be stuck without 
                cash as their payments may get delayed in future.”<br>
                <br>
                There are reports about bigger companies in the construction sector 
                forcing their suppliers to extend payment periods from 90 to upto 
                130-140 days. Nakheel, the construction major based in Dubai, 
                has claimedly been using payments as a bargaining chip with its 
                suppliers. The company is asking suppliers to either reduce costs 
                by 30 per cent and get the payment right away, or to wait for 
                two-to-three years to get their money. “It is important to keep 
                the money flowing and the government needs to intervene in the 
                matter,” says Thomas. As the cost of borrowing in international 
                markets have moved up, banks are renegotiating their interest 
                rates with companies. On an average interest rates have firmed 
                up by 0.75 to one per cent, adding to the financial burden of 
                companies.<br>
                <br>
                Fluctuation in commodity prices is another challenge for companies. 
                Corn and soya, the feedstuff for chickens make upto 70 per cent 
                of the cost of production for A’Saffa. These commodities are sourced 
                from various countries like Argentina, Brazil, America and India. 
                The price of soya has gone up from $470 per tonne in March 2008 
                to $530 per tonne in June 2009. Corn processes have escalated 
                from $310 per tonne in March 2008 to $330 per tonne in June 2009. 
                Says Mauly, “We have to absorb the increase in cost and this wreaks 
                havoc on our financials.”<br>
                <br>
                Apart from a price rise, businesses are also being forced to grapple 
                with uncertainty of supply. AATCO which uses ingredients like 
                tomato paste, pepper mesh, egg yolk and oils for its products 
                like ketchups, mayonnaise etc is faced with an unprecedented situation. 
                Says Mahesh Rao, general manager, AATCO, “A number of our suppliers 
                have become shaky and the stability of supply has become an issue.” 
                A number of it’s suppliers in China, the US and Turkey have been 
                inserting restrictive clauses like asking for the money upfront 
                before delivery, restraining the company’s cash flow.<br>
                <br>
                <strong>More than a match </strong><br>
                Undeterred by these problem, businesses have been working out 
                novel solutions to deal with the unprecedented situation. For 
                example, a carrot and stick approach is being employed to deal 
                with errant (cash) defaulters. Companies like NDC, Technique and 
                AATCO have worked out changes in their credit policy laying down 
                harsher credit terms for parties that do not pay on time, while 
                discounts are given to dealers who are ready to make a cash payment.<br>
                <br>
                As orders from traditional markets dry up, companies are looking 
                at new markets. All out marketing efforts are being made to break 
                into virgin markets. For example, Al-Waqia shoes is working on 
                tapping the Yemen, Jordan, and North African markets like Algeria 
                and Tunisia. And the efforts are yielding results – in Algeria, 
                it supplied 19,000 pairs of shoes in 2008, while in the first 
                six months of 2009 it has already sold 14,000 pairs. Tunisia which 
                ordered 16,000 pairs in 2008, has been supplied 10,000 shoes till 
                now this year. In Syria, the company got an agent who has ordered 
                10,000 pairs.<br>
                <br>
                Till November 2008, Al-Waqia had its hands full with existing 
                orders and was reluctant to chase new customers. A firm belief 
                that booking orders and not delivering on time, was a perfect 
                recipe for losing customers, the company went slow on scouting 
                for new clients. But a steep drop in sales from January this year, 
                forced the company to look for potential clients. “There was a 
                customer in Jordan who had been approaching us for a while, but 
                since we already had a lot of orders, we could not meet his requirements. 
                We wrote to him in March and have started delivering to him lately,” 
                says Ghuloom.<br>
                <br>
                Faced with a slowdown in the domestic market, A’Saffa diverted 
                the extra volumes generated by its capacity expansion to markets 
                like Qatar, Bahrain, Yemen and Egypt. In April 2009, the company 
                started exporting to Egypt. Yemen, where it was supplying a container 
                (25 tonnes) per month, is now being sold 2-3 containers a month. 
                The company is also exploring the possibility of exporting to 
                Malaysia and Singapore in future.<br>
                <br>
                Similarly, AATCO is targeting the West African markets like Senegal, 
                Mauritania, Luxury Coast, Liberia, Angola and Ghana. Says Rao, 
                “European and US manufacturers will be going slow on these markets 
                and so it is a good opportunity for companies like us.” Though 
                the company has a market in European countries like Holland, Italy, 
                UK, Denmark and others, it is looking at strengthening its presence. 
                The fact that AATCO guarantees good products and an assured supply 
                at a three to five per cent price advantage compared to multinational 
                companies gives it a competitive edge in present times.<br>
                <br>
                <strong>Exploring new product lines</strong><br>
                In an effort to leverage synergies, companies have also been looking 
                at related areas of business. National Gas for instance tied up 
                with Bharat Petroleum in November 2008 to supply Bharat Metal 
                Cutting Gas (BMGC) in Oman. BMCG is used for cutting steel and 
                works out to be 25 per cent cheaper than asytelene, the traditional 
                cutting option in the market. It also beats its competitor in 
                terms of storage – as seven cylinders of asytelene are equal to 
                one cylinder of BMCG. Says Sen, “We have captured 15 per cent 
                of the market since its launch in November 2008 and are looking 
                at a 50 per cent marketshare.”<br>
                <br>
                Inersys has plans of getting into natural gas, by setting up intermediate 
                stations to supply gases like Methane to industrial customers. 
                The gas is supplied from the source till the intermediate station 
                by the government and carried forward by the specialist company. 
                Inersys has tied up with RMG, a German company, which specialises 
                in setting up intermediate stations. It has also entered into 
                an agreement with SAG, an aluminium downstream firm to supply 
                such gas. National gas is also devising fire fighting systems 
                for users.<br>
                <br>
                Says Thomas, “The choice for companies is to either increase volumes 
                or price and since both of these are extremely difficult in the 
                prevailing circumstances, we have little choice apart from innovating.” 
                Technique has a specialist paint which is used to refurbish forts. 
                Though the company fabricated the paint six years back and used 
                it for painting one fort, the product has been largely unused 
                since then. The company is now looking at reviving the business. 
                A new manufacturing process to fabricate the paint has been put 
                in place and Technique is talking to the Ministry of Culture about 
                prospective business opportunities. Given the governments thrust 
                on renewable energy, potential has floated a division to look 
                into solar and wind energy as the company cash generator.<br>
                <br>
                Faced with an erosion of marketshare in the washing powder segment, 
                NDC relaunched its No. 1 detergent, in November 2008 with FWA 
                (fluorescent whitening agent). The relaunch was backed by a market 
                promotion helping the product to gain lost ground. To counter 
                the erratic supply of raw materials like sulphuric acid and benzene 
                the company has been braodbasing its sourcing options. “We buy 
                from China, India, Europe and our effort is to do beneficial material 
                substitution wherever possible,” says Sundaresan.<br>
                <br>
                Rejigging products to suit the changing tastes of the market is 
                something that other companies are also working on. AATCO has 
                introduced new product lines like honey mustard sauces, dressings, 
                vinaigrettes. Says Rao, “We have to innovate to attract consumers. 
                The effort is to give a value add for the same price.” The company 
                elicited a good response for its products during the Gulf Food 
                exhibition held from February 23-26, 2009 and it is looking forward 
                to Anuga, the world’s biggest food exhibition to be held in Germany 
                in October this year.<br>
                <br>
                NMWC has floated a new company called, Value Deal Trading (VDT). 
                The new company is supposed to service clients with NMWC’s other 
                products. The company has redesigned its trucks which carry its 
                bottled water brands like Tanuf and Salsabeel, to distribute other 
                goods. Thus the same vehicle is now used to distribute Tanuf tissues, 
                Juju packaged drinks, sparkling flavoured water and Red Bull (which 
                NMWC distributes). Says Chugh, “Our trucks are like shops which 
                can deliver anything. The choice is to either shrink and fit or 
                to adopt and grow.” The company’s average revenue per truck in 
                the last two years has grown helping it to do a turnover of RO7.2mn 
                in 2008. It has also entered into an agreement with Shaktibhog 
                Atta and Milkfood Ghee from India to distribute their goods in 
                the Sultanate. It will also be distributing the CAMEL brand of 
                nut products as a part of its deal with Seng Hua Hng Foodstuff. 
                A veggie wash that can be used to wash vegetables and fruits is 
                also on the cards.<br>
                <br>
                <strong>Tightening seatbelts</strong><br>
                <img src="images/cover03.jpg" width="368" height="305" hspace="9" vspace="3" border="0" align="right">Given 
                the slowdown, a rationalisation of costs is no longer a choice 
                but a necessity. Most companies are discovering the value of being 
                prudent. National Gas implemented an online fleet monitoring system 
                a year back. The IT system works as a logistics controller, giving 
                the company updates on how its trucks are being utilised. NMWC, 
                has rationalised its truck routes, for its 75 strong fleet. The 
                exercise has helped it to cut two routes. As offtake dropped in 
                the first quarter of 2009, the company realigned its routes – 
                so a truck covering four hypermarkets was given a few more supermarkets 
                on or close by, helping it to save money.<br>
                <br>
                Says Raisi, “There is no person or entity that has not been impacted 
                by the crisis. The good news is that every failure creates opportunities 
                for others to do the same business better.” The silver lining 
                is that the crisis is forcing companies to cut flab, reduce redundancies 
                and get more efficient. While the fittest will survive the weak 
                and inefficient will perish. After all getting back on one’s feet 
                whether it is a game of squash or a business requires agility 
                and fitness.</font><br>
