						<p style="margin-left: 5; margin-right: 5" align="justify">
						<font face="Verdana" size="1">The major lesson to be 
                        learnt in my view is that no matter how big you become, 
                        you need to constantly reinvent yourself and the failure 
                        to do this led to GM’s doom.</font></td>
