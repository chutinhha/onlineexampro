                      <td colspan="3" height="25" bgcolor="#D64F30" width="774">
						<p align="center" dir="ltr">
						<b><font face="Verdana" size="1" color="#FFFCE8">WHAT IN 
                        YOUR OPINION LED TO THE DOWNFALL OF ONE OF THE BIGGEST 
                        COMPANIES IN THE WORLD?</font></b></td>
  </tr>
  <tr>
                      <td height="104" valign="top" width="351"> 

<p align="justify"><font face="Verdana" size="1">The fundamental reason for the 
downfall of GM is underscored by the Bernstein Research which attributed the 
collapse of GM to adopting a strategy of cutting prices to improve sales and 
keep the show going instead of dealing with the real issues of rationalising 
products, manufacturing capacities and distribution network. Secondly, GM had 
over the years accumulated huge chunks of unproductive costs such as 
compensation paid to unionised workers who were laid off due to automation, 
which did not give them any value and made the company non-competitive as 
compared with other non-American manufacturers. </font></p>

						</td>
    <td width="5" height="104" bgcolor="#BCBEC0"></td>
                      <td height="104" valign="top" width="402"> <p align="justify" style="margin-left: 5; margin-right: 5">
						<font face="Verdana" size="1">Even as recent as last 
                        year they had been talking of a ‘Hummer centered’ sales 
                        push strategy , which shows how much out of touch with 
                        market reality they had been. The American consumer had 
                        been going for more fuel efficient compact cars while GM 
                        had been continuing to promote big cars which were 
                        nowhere as efficient as the Japanese vehicles. Further 
                        their inability to cut costs has been another reason for 
                        their downfall.<br>
&nbsp;</font></td>
  </tr>
