                      <td colspan="3" height="34" bgcolor="#4078A9" width="774">
						<p align="center" dir="ltr">
						<b><font face="Verdana" size="1" color="#FFFCE8">SHOULD 
                        GM BE LIQUIDATED OR REVIVED?</font></b></td>
  </tr>
  <tr>
                      <td height="96" valign="top" width="351"> <p align="justify" style="margin-left: 5; margin-right: 5">
						<font face="Verdana" size="1">The answer seems to lie in 
                        selecting the lesser of the two evils. The social and 
                        political costs of liquidation would be very high and 
                        disruptive with a huge domino effect. On the other hand, 
                        revival would cost as much and may even fail if GM does 
                        not shed many of its fixed obligations and does not 
                        recover from the sins of the past through an accelerated 
                        implementation of the turnaround programme. Chapter 11 
                        bankruptcy appears to be the most promising way forward 
                        for GM to reduce its debts (which is twice its total 
                        assets) and compel settlements which would reduce its 
                        operating costs. Sale of Hummer, Saturn and SAB brands, 
                        continuing with fewer dealerships, development of 
                        smaller fuel efficient vehicles to substitute its 
                        previous focus on SUV’s, etc., would prevent the cash 
                        drain and may restore long-term viability of GM.</font></td>
    <td width="5" height="96" bgcolor="#BCBEC0"></td>
                      <td height="96" valign="top" width="402"> 

<p align="justify"><font face="Verdana" size="1">GM is a typical example of a 
corporation that has grown so big that it has lost control over itself. Over the 
years they had developed such rigidity in their operating styles that very 
little initiative or innovation could thrive in that ambience. I have personally 
dealt with them for supply of batteries through their China after market centre 
and was amazed to see a complete lack of initiative or enthusiasm. Their tender 
documents would run into 100s of pages even to buy a few dollars worth of parts. 
There was a complete disconnect in their buying systems between quality 
prequalification and online bidding processes. The reason is their 
‘one-size-fits-all’ approach to buying. This is the reason, I have stated that 
in their present form they could neither be revived nor restructured. The only 
solution is to split them into more manageable sizes. </font></p>

                      </td>
  </tr>
