                      <p align="center"><b> <font face="Verdana" size="1" color="#FFFCE8">
                      WHAT LESSONS CAN OTHERS LEARN FROM GM, A 101 YEAR OLD 
                      TITAN?</font></b></td>
  </tr>
  <tr>
                      <td height="82" valign="top" width="351"> <p align="justify" style="margin-left: 5; margin-right: 5">
						<font face="Verdana" size="1">Firstly, serious financial 
                        problems need to be addressed expeditiously and 
                        strategically. The financial losses in ’05, ’06, ’07 &amp; 
                        ’08 warranted a serious relook at the company’s cost 
                        structures and product mix rather than taking the easy 
                        way out of lowering prices. Secondly, creating and 
                        retaining competitive advantage is a necessity. GM’s 
                        business model was out of sync when compared with the 
                        likes of Toyota and Honda, which had a significant 
                        impact on business results. Thirdly, GM could not afford 
                        to be caught in a web of relationships designed for 
                        another era. </font></td>
    <td width="5" height="82" bgcolor="#BCBEC0"></td>
