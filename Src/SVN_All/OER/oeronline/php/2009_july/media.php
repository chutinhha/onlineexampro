                    </font><b><font face="Verdana" size="4">Pushing the Envelope</font><br>
                    <br>
                    <font face="Verdana" size="1">Al Wisal is redefining radio 
                    with its innovative programming, state-of-the-art infrastructure 
                    and customised options for clients. Mayank Singh reports </font></b> 
                    <font face="Verdana" size="1"><b><br>
                    <br>
                    </b>Though Private FM radio stations started just over two 
                    years ago, with Hala FM taking off in May 2007 and Al Wisal 
                    in March 2008, the media has taken remarkable strides in a 
                    short span. Al Wisal, the Arabic FM radio station from SABCO 
                    Media has claimedly acquired a market penetration of over 
                    35 per cent, in a year since it was launched. The station 
                    set a record of sorts by penetrating 18 per cent of the market 
                    within six weeks of its launch. Penetration levels are based 
                    on the number of people who heard a radio station a day before.<img src="images/media1.jpg" width="200" height="173" hspace="9" vspace="10" align="right"><br>
                    <br>
                    And the initial success of the media has not gone unnoticed. 
                    Says Swaminathan J, media manager, Universal Media, “Radio 
                    has evolved as a media and clients are serious about using 
                    it. Radio has certainly become a part of the media plan of 
                    clients, but since Oman is a cost sensitive market clients 
                    are cautious about the amount of money that should be invested 
                    in radio.”&nbsp; <i> Reinforcing the message</i><br>
                    <br>
                    Radio serves as a big reminder medium. It also gives clients 
                    the choice of communicating with various segments of the market 
                    at different times of the day, across a wide geographic spread, 
                    whether they are travelling in cars, sitting in coffee shops 
                    or shopping at malls. Says Eihab Abutaha, general manager, 
                    SABCO Media, “FM entertains listeners and it is a powerful 
                    platform for clients to reach out to their audience interactively 
                    and effectively. <br>
                    <br>
                    Second, radio lends itself to creating “out-of-the-box” solutions 
                    for clients and the sky is the limit. Al Wisal has created 
                    promotions for BankMuscat, Qurum City Centre (QCC), Nissan 
                    Nawras and many others. The station did a promotion out of 
                    QCC, wherein live broadcasts were done involving shoppers, 
                    outlets and the various brands on display. According to QCC 
                    officials, the promotion led to a 30 per cent increase in 
                    footfall. In the automobile sector, live broadcasts out of 
                    showrooms helps dealers to attract buyers. Radio, by any standard 
                    is an extremely powerful media when used effectively.<br>
                    <br>
                    The clutter in pan Arab television makes it difficult for 
                    media buyers to take a pick. “There are 1,100 television channels 
                    to choose from in the region, making the medium extremely 
                    fragmented,” says Swaminathan. A back of the envelope calculation 
                    shows the cost effectiveness of radio. Creating a 30-second 
                    television commercial on a popular channel can cost as much 
                    as RO50,000. A full page colour advertisement in Gulf News 
                    can cost as much as Dhs50,000 (RO5,000). In local dailies 
                    in the Sultanate this can range upwards of RO3,000 (card rate 
                    before discount). Compared to this a 30-second spot on Al 
                    Wisal costs RO70-120 based on the programme on which the commercial 
                    plays out. “Thus for the same amount of money one can get 
                    far more frequency and depth on radio compared to other media,” 
                    says Eihab. <br>
                    <br>
                    <b>Market segmentation <br>
                    </b>Segmentation of listeners is another strength that radio 
                    offers. As a rule of thumb 70-75 per cent of radio listening 
                    happens while driving. And with traffic snarls getting worse 
                    by the day, the time spent on the roads by commuters is growing, 
                    which augurs well for the medium. On the flip side appealing 
                    to this variegated audience poses its own set of challenge. 
                    The fact that Oman has a big base of Arabic listeners, unlike 
                    Dubai, which has a predominant expatriate population makes 
                    the task more onerous. Generating high quality content is 
                    thus a necessity. Al Wisal has gone about defining its programming 
                    based on the needs of its listeners. Says Eihab, “The content 
                    is discussed and debated minutely as a part of the internal 
                    strategic approach because content development is what defines 
                    you as a radio station, in terms of character, style and personality.”<br>
                    <br>
                    The stations programming revolves around music and talk shows. 
                    It also has news on the hour, keeping listeners updated. The 
                    station is constantly upgrading content in line with the evolving 
                    style and tastes of listeners. But with numerous stations 
                    on air, do people have a tendency to switch between stations? 
                    Eihab counters the charge, “People switch only during non-prime 
                    time. Our music selection is fine tuned – in terms of how 
                    to play, what to play and when to play that it minimises switching. 
                    Radio is generally a new medium and people may switch to experiment 
                    or to enjoy a song elsewhere. However, there will always be 
                    a favourite.” Al Wisal is working on projecting a brand image 
                    and character that conveys its core values of being joyful, 
                    dynamic and yet being socially responsible and closely connected 
                    with its community. The various colours used in the brand 
                    reflect the stations promise of being an enjoyable, mature 
                    and exciting medium. Piscatchio green highlights growth and 
                    development; light burgundy stands for togetherness and magenta 
                    is about a feeling of community and people.<br>
                    <b><br>
                    Some way to go<br>
                    </b>Oman with its mountainous terrain and a geographically 
                    dispersed population poses a big challenge for radio stations. 
                    To reach communities beyond the Batinah area requires strong 
                    transmission equipment. Al Wisal has doubled its transmission 
                    strength in the last two months reaching out to new listeners. 
                    The station also started broadcasting out of Salalah recently. 
                    <br>
                    <br>
                    The lack of a strong FMCG sector in terms of advertising is 
                    another lacunae for radio stations in Oman. Says Swaminathan, 
                    “Oman is an automobile and banking dependent market, whereas 
                    television and radio thrive on products that are all about 
                    touch, feel and buy.” Though the government has relaxed rules 
                    pertaining to radio, a number of operators are still to launch 
                    operations. Thus there are only three private radio stations 
                    on air right now – Al Wisal, Hala and Hi FM. The government 
                    has its own stations like Oman FM.<br>
                    <br>
                    Eihab sums up, “Media is an important strategic growth area 
                    for the SABCO Group. SABCO Media is a unique platform of communication 
                    channels in radio, outdoor and publishing and Al Wisal is 
                    an important pillar under this platform.</font> 
