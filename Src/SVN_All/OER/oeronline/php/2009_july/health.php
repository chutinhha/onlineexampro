                    <br>
                    </b> <font face="Verdana" size="1"><b>By now almost everyone 
                    the world over is well aware of the latest pandemic to wreck 
                    havoc with our day-to-day lives. Quite a few people live in 
                    fear of contracting the disease, but is this fear justified? 
                    Malcolm Xavier Crasta speaks to Dr Idris Alabaidni, acting 
                    director of the department of communicable disease surveillance 
                    and control to find out<br>
                    </b><br>
                    At present there are over 100 confirmed cases of the H1N1 
                    influenza in the Middle East and on June 13th, the first three 
                    cases were confirmed in Oman. It was carried in by three stud<img src="images/health1.jpg" width="150" height="133" hspace="9" vspace="3" align="right">ents 
                    who had just arrived into the Sultanate from the US. As of 
                    now they have been sent home after treatment. Which begs the 
                    question – Is it really all that dangerous?<br>
                    <br>
                    The strain that the world is currently fighting was first 
                    identified in April 2009 and was declared a level 6 pandemic, 
                    by the WHO, on June 11th. But, as serious as it sounds, it 
                    is not nearly as bad as most people perceive it to be. The 
                    mortality of the disease is limited since it is not a severe 
                    infection and shares most of its symptoms with regular flu. 
                    In fact, the case fatality rate for the current pandemic is 
                    only 0.5 per cent. The primary reason for a low rate is the 
                    fact that the Tamiflu drug works as an effective countermeasure 
                    for the disease.<br>
                    <br>
                    <b>Root cause<br>
                    </b>The primary reason for the pandemic is the fact that this 
                    is an all new strain of the virus and as such we have little 
                    or no immunity to the disease and since it is transmitted 
                    just as easily as regular flu, it spreads extremely easily. 
                    And while it cannot be stopped from entering Oman (and at 
                    the time of this article it has already done so) it can definitely 
                    be contained and mitigated. At present a vaccine is being 
                    developed to counter the disease but the first doses won’t 
                    be available till September and even then in limited numbers.<br>
                    <br>
                    Rest assured, in the mean time, the Ministry of Health is 
                    doing everything in its power to reduce and contain the effects 
                    of the pandemic. “The Ministry of Health would like to assure 
                    the population that it has taken all the necessary steps to 
                    confront the disease” says Dr Idris. He also added “The H1N1 
                    committee has also taken steps to ensure the quantity and 
                    availability of the Tamiflu drug.” In addition to this there 
                    is ongoing training all over the country to detect, deal with 
                    and treat H1N1 and measures are already being taken to prevent 
                    the spread of the disease. “An early warning system and surveillance 
                    of the influenza is already in place and all ports of entry 
                    are being looked into” added Idris. The WHO has not implemented 
                    any travel restrictions, but it has stated that it is better 
                    to avoid affected countries unless absolutely necessary. The 
                    only real fear that the experts have is that the influenza 
                    virus can mutate to become an even more potent strain and 
                    also become less susceptible to vaccines developed to cure 
                    the earlier strain. But the chances of this happening are 
                    slim and only if it does, should people have anything to worry 
                    about.<br>
                    <br>
                    The current outbreak of the ‘swine flu’, as most people know 
                    it, is being caused by a new strain of the Influenza A virus 
                    subtype H1N1. But this new strain was found to be a reassortment 
                    of four known strains of the H1N1 virus – North American Mexican 
                    influenza, North American avian influenza, human influenza 
                    and swine influenza virus. While this strain is not as deadly 
                    as most people believe it to be (more on that later), they 
                    do have a reason to fear it. The H1N1 strain is by far the 
                    most common form of influenza in humans and one strain of 
                    the H1N1 is also responsible for one of the most deadly pandemics 
                    in human history.<br>
                    <br>
                    This particular outbreak was known as the Spanish flu and 
                    hit the world between 1918 and 1919. It is officially stated 
                    to have caused the death of close to 21mn people, but the 
                    true estimated death toll is said to be anywhere between 50mn 
                    to 100mn and infected as many as 500mn worldwide. But let’s 
                    look at the facts – this particular strain was very odd because 
                    it killed mainly young healthy adults, quite unlike the regular 
                    virus that generally affects babies, the old or chronically 
                    ill. Also, thousands that died from the disease were soldiers 
                    fighting in the war. And, let’s face it, medical science has 
                    advanced in leaps and bounds since 1918 and we are more prepared 
                    than ever before to battle such an outbreak.</font>
