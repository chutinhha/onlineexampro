                    <strong>Opening the floodgates </strong><br>
                    India’s dizzying economic ascent began in 1991, when the government 
                    abruptly dismantled the “license raj,” a system of tight controls 
                    and permits in place since independence in 1947. Unlike China, 
                    India has not undergone an industrial revolution. Its economy 
                    is powered not by manufacturing but by its service industries. 
                    In a vast subcontinent of poor farmers whose tiny holdings 
                    shrink by the decade, a highly competitive, if small, technology 
                    sector and a welter of service businesses have helped create 
                    a middle class, materialistic and acquisitive, along with 
                    some spectacularly rich entrepreneurs. The author does a superb 
                    job of explaining the new Indian economy and why its transformation 
                    qualifies as strange. <br>
                    <br>
                    The euphoria about India’s capability in information technology, 
                    economic might and superpower potential has of late been tempered 
                    by an awareness of just how much darkness is still beneath 
                    the lamp. Study after study has revealed this sobering fact. 
                    According to one, India still has only 84 television sets 
                    per 1,000 people (America has 938); 7.2 personal computers 
                    for every thousand people (Australia has 564.5); and the internet 
                    reaches only 2 per cent (Malaysia’s figure is 34 per cent).<br>
                    <br>
                    <strong>Polar opposites</strong><br>
                    Several recent books have examined the savage inequalities 
                    between the country’s burgeoning, educated, urban elite and 
                    the shockingly poor who live in the vast hinterlands. Luce’s 
                    thoughtful and thorough book is an unsentimental evaluation 
                    of contemporary India against the backdrop of its widely expected 
                    ascent to great power status in the 21st century’ - fits right 
                    into this category. He suggests the dichotomy of India in 
                    the book’s subtitle and later calls India’s rise ‘strange’ 
                    because, while becoming an important political and economic 
                    force, it has remained ‘an intensely religious, spiritual 
