                    Edward Luce is better placed than most to write this book. 
                    Between 2001 and 2005, he was the Financial Times’s New Delhi-based 
                    South Asia bureau chief and his wife is Indian. His combination 
                    of closeness and distance lends him objectivity and credibility. 
                    In Spite of the Gods is not afraid to tackle the big question, 
                    which is how the rise of China and India might alter the geopolitical 
                    map of the world. Towards the end of the book, Luce elaborates 
                    on how the relationship between the two Asian countries has 
                    altered and how America, suspicious of India during the Cold 
                    War years, has warmed to it more recently. The US, he says, 
                    would want to promote better ties with India to counterbalance 
                    China’s emerging dominance and ‘prolong American power in 
                    the coming decades.’ <br>
