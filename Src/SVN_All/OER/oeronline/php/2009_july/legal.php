                <p align="justify" style="margin: 3px 10px" dir="ltr"><font face="Verdana" size="1"><br>
                    </font><b><font face="Verdana" size="4">JUDICIAL SYSTEM IN 
                OMAN</font><br>
                <br>
                </b>
                <font face="Verdana" size="1"><b>The Sultanate of Oman has 
                maintained an integrated modern legislation and courts which are 
                comparable to their counterparts in the developed countries. 
                Here is a look at the origin and development <br>
                of this remarkable system</b></font><p align="justify" style="margin: 3px 10px" dir="ltr">
                <font face="Verdana" size="1"><b><br>
                </b>
                <img src="images/media01.jpg" width="234" height="191" hspace="9" vspace="3" border="0" align="left">The legal and judicial systems in Oman have witnessed 
                remarkable development since the beginning of the Renaissance 
                under the wise leadership of His Majesty Sultan Qaboos Bin Said. 
                The Sultanate of Oman has maintained an integrated modern 
                legislation and courts which are comparable to their 
                counterparts in the developed countries. In order to achieve 
                these objectives a Ministry of Justice was established in the 
                Sultanate which was followed by a specialised Ministry of legal 
                affairs. It is worth mentioning that prior to the year 1971 the 
                judicial system in Oman was limited to Shariah courts in Muscat 
                and other courts were scattered in the different regions. Such 
                courts were composed of a single judge and a secretary who used 
                to hold sessions at Wali Majliz. At that point of time the 
                Sariah Judiciary had the general jurisdiction to entertain all 
                civil, criminal suits and other Shariah matters.<br>
                <br>
                Due to the development witnessed in the economic, social, 
                commercial, and political life in the Sultanate of Oman, a 
                number of legislations have been issued. The legislation in the 
                Sultanate can be divided into two – the first of which is 
                represented by the laws which constitute the main legislations 
                and are normally issued by Royal Decrees whilst the second type 
                is a subordinate legislation normally issued in the form of 
                Ministerial decisions and Executive Regulations by the cabinet 
                of Ministers or the concerned Ministries by virtue of specific 
                powers granted by virtue of Royal Decrees. <br>
                <br>
                The basic law of the state comes at the top of the hierarchical 
                legislation system in the Sultanate of Oman and it is known as 
                the basic law. His Majesty in November 1996 issued Royal Decree 
                No. 101/1996, which forms the basic law of the state. The said 
                law contains a number of matters and aspects that specifies the 
                form of the state and its political system, public authorities, 
                rights, freedoms, duties and economic and social affairs as well 
                as the political principles of the Omani community. Article 2 of 
                the basic law of the state provides that Islam is the official 
                religion of the Sultanate of Oman and that the Shariah 
                provisions are the basis of legislation and other laws.<br>
                <br>
                The enactment of these modern legislations calls for the 
                establishment of new courts, hence Police penal code has been 
                established by virtue of the Royal Decree No.(7/74) and 
                thereafter the said court become the Penal court which has been 
                established by virtue of the Royal Decree No.(25/84). Also the 
                authority for the settlement of commercial disputes was 
                established by virtue of the Royal Decree No.(13/97) to be the 
                judicial authority having jurisdiction to entertain commercial 
                dispute and thereafter it has been replaced by the commercial 
                courts by virtue of the Royal Decree No. (13/97). The land 
                affairs committee has been established by the Royal Decree No. 
                (6/72) as well as the lease dispute committee which has been 
                established by the Royal Decree No. (4/73).<br>
                <br>
                <b>Overall jurisdiction <br>
                </b>Whereas the above mentioned courts and committees, which 
                have judicial jurisdiction were not subject to a single body, 
                they were subjected to a number of bodies in charge of 
                supervision and organisation of their affairs. The remarkable 
                legislative development in the Sultanate and the practice of the 
                judicial bodies and the various courts all of which have 
                necessitated the integration of the judicial system in the 
                Sultanate of Oman into one hierarchy body at the top of which 
                comes the Supreme court which is in charge of the integration of 
                the judicial principles. The basic law of Oman has challenged 
                the problem of the integration of the judicial system and its 
                independence by providing for the same in a special chapter 
                enacted mainly for the judicial system and the principle of the 
                sovergnity of law. In order to achieve this objective the first 
                judicial authority law in the Sultanate has been issued by 
                virtue of the Royal Decree No.(90/99) on November 21, 1999 which 
                has been followed by the Royal Decree No. (91/99) which 
                established the Administrative court. The administrative court 
                has jurisdiction to entertain administrative disputes. The 
                public prosecution has been established by virtue of the Royal 
                Decree No. (92/99) which is in charge of the public suit filed 
                on behalf of the community and supervises the judicial control 
                affairs and it applies penal laws and other jurisdictions 
                provided in the law. The public prosecution is a separate 
                judicial entity whilst in the past it was subjected to the 
                supervision of the police general inspector from an 
                administrative point of view. <br>
                <br>
                Taking into consideration the huge Royal decrees and 
                legislations enacted, the Sultanate is currently maintaining an 
                integrated judicial system comprising of two types, the first of 
                which is an ordinary judicial system represented by the various 
                courts scattered in the different regions and wilayats of the 
                Sultanate arranged in a hierarchal manner starting by the 
                primary courts, court of appeal which entertain appeal filed in 
                respect of the judgment passed by the primary court. At the top 
                of the hierarchy is the Supreme court located in the Muscat 
                governorate, and the Supreme court being a court of law has a 
                jurisdiction in respect of petition by cassation filed in 
                respect to the judgment issued by the courts of appeal. However, 
                the work is these courts has been divided between various 
                circuits to entertain civil, commercial, Shariah, labour, penal 
                etc. suits. The second type of the judicial system is 
                represented by the administrative court which is considered to 
                be one of the most important courts when compared with its 
                counterparts in the Arab Region. However the Sultanate of Oman 
                is proud of the judgment issued by this court which respects the 
                sovergnity of the law regarding the judgment issued by the 
                council of the French republic which stands as an initiator of 
                the Administrative judiciary in the world. The administrative 
                court established an appeal circuit to entertain appeals and 
                petitions filed in respect to the judgments issued by the 
                preliminary circuit of the administrative court. Moreover, the 
                administrative court established a branch in the wilayat of 
                Salalah to entertain administrative disputes that could take 
                place in the Dhofar region and in order to lessen the burdens on 
                the shoulders of the disputing parties.<br>
                <br>
                In brief, the Sultanate currently enjoys an independent judicial 
                system, which is deemed to be one of the most important 
                securities and tools for the achievement of the sovergnity of 
                law in Oman. Moreover the well established judicial system in 
                and the legislation pertaining to investment has played and is 
                still playing a vital role in attracting investment to the 
                Sultanate apart from the role of development by the 
                privatisation projects. The legal legislations enacted in the 
                Sultanate has helped in the stability of the bank transactions 
                taking into consideration that banks and finance institutions 
                have a major role in the comprehensive developments in the 
                Sultanate of Oman. However, many investors feel comfortable to 
                invest in the Sultanate of Oman without any hesitation due to 
                the sufficient securities provided for each investor, the matter 
                which could be evident from the foreign investment brought in 
                the Sultanate of Oman during the recent years. </font>
