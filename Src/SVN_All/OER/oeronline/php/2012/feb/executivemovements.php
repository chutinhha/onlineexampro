						<p align="justify" class="style10">
				Haya Water has recently appointed Hussain Hassan Ali 
				AbdulHussain as the new Chief Executive replacing Omar Al 
				Wahaibi who left to take charge as the CEO of Electricity 
				Holding Company. The new CEO brings with him 24 years of 
				experience in oil and gas projects, operations, engineering and 
				commercial developments in Oman. AbdulHussain says he relishes 
				the challenge to lead one of Oman’s most dynamic, progressive 
				and innovative companies.<br>
				<br>
				A graduate from the University of Arizona, AbdulHussain spent 14 
				years working for Petroleum Development Oman (PDO) before moving 
				to the Oman Oil Company and then helping with the creation of 
				Oman Petrochemical Industries Company (OPIC). He subsequently 
				moved to work at Oman Refineries and Petrochemicals Company (ORPC) 
				as business development and marketing general manager before 
				leaving to establish his own engineering support and services 
				business.<br>
				<br>
				Commenting on his appointment AbdulHussain says: “I am truly 
				honoured to be selected by the board of directors of Haya to 
				lead the executive management team. Together we will continue to 
				work with the same level of diligence, care and enthusiasm to 
				deliver the mission and vision entrusted to Haya. “I have 
				already been impressed by the professionalism, commitment and 
				expertise of the team here at Haya and I am looking forward to 
				working with them to achieve our goal of making Muscat a better 
				city through the implementation of our world-class water reuse 
				project. Haya Water chairman HE Sultan Hamdoon Al Harthi 
				welcomed AbdulHussain to the company and highlighted the 
				achievements and the wealth of experience that the new chief 
				executive is bringing to help Haya achieve its mission to roll 
				out the most advanced and sophisticated waste water treatment 
				technology across the Muscat governorate.<br>
