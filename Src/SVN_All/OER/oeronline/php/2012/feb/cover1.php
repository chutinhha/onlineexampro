						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">
						Fresh perspective to growth</font></b><p align="justify" class="style13" dir="ltr">
				<br>
				<b>Focus on human resources<br>
				</b>According to Shankar Kailasam, senior vice president – asset 
				management, Gulf Baader Capital Markets, the government has 
				given more push towards the human resource development in 2012 
				Budget by increasing the spending on education, training, 
				healthcare, housing and water etc. “This would effectively 
				improve citizens’ lives which we believe would bring in 
				positivism towards achieving stable economic growth in the 
				coming years. The total investment in development expenditure of 
				civil ministries of RO1.4bn would drive the real investment in 
				the economy by means of providing opportunities for private 
				sector companies and achieve the objective of new jobs creation, 
				which is commendable,” Kailasam adds.<br>
				<br>
				The government is also expected to spend RO1.3bn on the basic 
				and general education for 518,000 students, as well as spend on 
				85,000 internal and external missions including the existing and 
				the additional missions that were approved at the end of 2011. 
				Kailasam says that this incremental investment towards the 
				education and training would enhance knowledge base towards 
				achieving excellence in wider fields of economic development.<br>
				<br>
				Mohammed Sajid Khan, country manager – Oman, ACCA (the 
				Association of Chartered Certified Accountants) feels it’s an 
				excellent budget thanks to its sustained focus on human capital 
				development and infrastructure. “We have always been 
				appreciative of the emphasis laid in Oman on the development of 
				the most important of all the resources – human capital -- which 
				is vital for sustainable economic development and also to 
				achieve the Vision 2020 targets.”<br>
				<br>
				Equally important is the development of new infrastructure and 
				the improvement of existing facilities which play a significant 
				role in meeting the needs of a fast developing nation and 
				increasingly becoming more attractive to multi national 
				companies and foreign investors due to government’s 
				concentration on the productive and industrial projects and the 
				steadiness of the social and political situation, says Mohammed.<br>
				<br>
				However, the budget should go beyond the spending in the 
				education and health sectors, opines Hussain while stating that 
				the budget is more focused on the spending side rather than on 
				investment. “I haven’t seen enough room in the budget to create 
				high quality medical personnel and increase the efficiency 
				levels in hospitals which at the moment is low. The diagnostic 
				capabilities are poor except for few diseases. That is why 
				Omanis go to different countries, like India, Thailand, and 
				Europe. Job creation is appreciated, but by just creating jobs 
				without imbibing the job culture, ethics and values, will not 
				produce desired and sustainable results to the nation. We also 
				need to increase the quality of education and teachers as well. 
				This is where the actual development of human resources should 
				take place. Merely constructing more hospitals and schools only 
				is not good enough,” Hussain avers.<br>
				<br>
				<b>Training</b><br>
				“With allocation of RO1.3bn (13 per cent of total public 
				expenditure) for the educational sector and re-introduction of 
				5,000 training opportunities linked with employment, it’s 
				encouraging to see the strong emphasis on this area,” says 
				Mohammed. As far as training is concerned, ACCA estimates that 
				there is a need of almost 20,000 jobs in the accountancy 
				profession alone as the private sector continues to expand.<br>
				<br>
				The increased focus on training aspect has come in for praise 
				from Dr Amer Rawas, CEO of Omantel. “We are very delighted as 
				Omanis, as a national company and as a business that such a 
				level of detail was announced, so very significant transparency 
				that allows us to analyse the content. More emphasis by the 
				government on the training will mean that we will get people who 
				already have the skills.”<br>
				<br>
				Omantel hopes that it can take part in the government’s training 
				programme as the company has its own training centre known as 
				the Oman Telecommunications Institute which is ready to train in 
				the field of telecommunications. It has worked in the past with 
				the ministry of manpower to train Omanis for the telecom market 
				and in most cases, they were all successfully employed after 
				training, he informs.<br>
				<br>
				“Now we have a whole new strategy as a result of the training 
				emphasis laid by the government which has been presented to the 
				management pending few changes to develop and strengthen and 
				fill the capacity of the Oman Telecommunications Institute to 
				contribute to the government’s goals and benefit the society,” 
				Dr Amer adds.<br>
				<br>
				Continuing further, he says, “We have had a success story on 
				this. Our training centre and our engineers have worked towards 
				moving people from smaller jobs to better jobs. For example, we 
				have had people who were drivers and support staff and labourers 
				whom we trained on splicing of fibre which is a technically 
				complex job but these people are now connecting Oman. Now this 
				is what we want to do. Move people to better jobs. In 2011, we 
				were able to move about 60 people from the call centre into the 
				shops which is seen as a job progression.”<br>
				<br>
				<b>SME development<br>
				</b>Although much needed importance is being given to SMEs in 
				Oman with recent Government initiatives like guaranteeing 50 per 
				cent of the loans, there isn’t much clarity in the budget about 
				what role the SMEs will play in this year’s planned development, 
				points out Mohammed.<br>
				Dr Abdullah also agrees that SME development is not spelt out 
				clearly even though subsidies have been announced (see Chart 6). 
				“If a big multinational company comes to the country and imports 
				its employees, engineers, capital and technology and executes a 
				project, how do we benefit? This is where the SMEs can play a 
				role. Because if the government can take some measures to ensure 
				that SMEs will get some sub-contracts from this large company, 
				then in addition to the infrastructure development, the SMEs 
				will also benefit from these projects.”<br>
				<br>
				Nick Smith, CEO Eight Group of Companies, has this to say, 
				saying, “We are a medium sized company that are into premier 
				landscape design, construction and maintenance. When I see the 
				Budget, there are some words which gives support to SMEs but 
				without spelling out how this segment will come up. We struggle 
				to grow our business. There is a lot of bureaucracy, there is 
				difficulty getting visas and we still have issues with getting 
				finance from banks. And I know a lot of other people who are 
				running SMEs facing similar issues. They want to employ people 
				and grow their businesses but they are struggling with the 
				complexity of what is needed. And also with the different 
				messages given out by some of the different ministries.”<br>
				Continuing further Nick says, “it’s great that there are 
				positive messages but really we need more meat. We need some 
				concrete proposals. And I know there are a lot of people who are 
				thinking on similar lines. The government accorded top priority 
				to job creation aiming to employ 36,000 people in the public 
				sector. How are we going to do it? That is kind of unresolved. 
				What we do know is that some of these jobs are real and some of 
				them will take people from the private sector and put them in 
				the government. What it should actually be doing is putting 
				equal number of people in the private sector as well; but I 
				don’t see how that is going to happen.”<br>
				<br>
				<b>Non-oil sectors<br>
				</b>There is an increase in the allocation of budgets for the 
				non-oil sector. The combined allocation for supporting tourism 
				projects and building infrastructure is an indication of the 
				Sultanate’s commitment to directly invest in the tourism and 
				hospitality industry. With the nation’s drive to boost tourism 
				by attracting 12 million visitors by 2020, it is crucial to have 
				sufficient infrastructure to ensure that more hospitality venues 
				are available to meet this anticipated growth.<br>
				<br>
				“The 8th Five Year Plan’s aims to increase and improve 
				transportation links through road infrastructure (see Chart 4) 
				particularly outside Muscat, rail road scheme, seaports (Salalah, 
				Hasik, Al Shuaymiya and Masirah) and Airports (Muscat, Salalah, 
				Sohar, Duqm, Adam and Ras Al Hadd) will increase tourism and 
				boost the economy,” says Wael Al Lawati, CEO, Omran. According 
				to Wael, the diversification strategy stemming from Vision 2020 
				witnessed first-hand industrial developments in Sohar, Duqm and 
				Salalah free zones and ports which in itself resulted in better 
				roads, value added services and gave a development boost to the 
				cities. The infrastructure development and transportation links 
				are the very pillars that will create a robust economy. <br>
				<br>
				Omran invests in projects that have a long term return on the 
				economy and community and which have a ‘multiplier effect’. 
				Omran’s OCEC Project is a good example, says Wael. The initial 
				labour histogram analysis indicates approximately 2,000 Omani 
				jobs will be generated on average during the construction period 
				and there will be an increase in business to sectors such as 
				airlines, hotels, restaurants, tour operators, retailers and 
				taxi drivers injecting revenues into the national economy in 
				abundance, which then trickles down.<br>
				<br>
				<b>Outlook</b><br>
				The government is also taking steps to encourage private foreign 
				and local investments including the development of small and 
				medium enterprises through creating proper investment 
				environment, providing facilities and incentives in creating new 
				industrial estates across various regions aiming at diversifying 
				growth.<br>
				<br>
				All these efforts taken towards diversification of economy, 
				believes Kailasam, would in turn lead to healthy investment 
				opportunities for the private sector to move forward. The 
				government’s focus on the creation of more partnerships with the 
				private sector towards the creation of robust production and 
				investment projects is expected to bring in value addition to 
				the economic growth momentum.&#8194;<br>
