						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">Luxurious 
						positioning</font></b><p align="center" style="margin: 10px; ">
				<font face="Verdana" size="1">
				<img border="0" src="images/autotalk-feb-12-1.jpg" width="510" height="250" style="border: 1px solid #808080" class="style4"></font><p align="justify" style="margin: 10px; ">
				<font face="Verdana" size="1">
				Hyundai’s Genesis Prada blends high quality automobile 
				engineering with elegance and panache. Mayank Singh reports from 
				Dubai.<br>
				<br>
				How does one describe an association between the fastest growing 
				international automobile brand and one of the world’s best 
				luxury brands – a perfect fit, match made in heaven or luxury on 
				wheels – well, it may be apt to use all of these epithets. 
				Hyundai Motor Company, recently announced the launch of the 
				limited edition Genesis Prada in the Middle East. The Genesis 
				Prada, based on the 2012 Genesis sedan and featuring Hyundai’s 
				award-winning Tau 4.6 MPI engine was unveiled to over 100 VIP 
				guests, company executives and media colleagues at an exclusive 
				event held at the Park Hyatt hotel in Dubai recently.<br>
				<br>
				Genesis Prada is characterised by a special design that combines 
				Prada’s sophisticated innovation, attention to detail and 
				uncompromised quality approach with Hyundai’s advanced 
				technology and expertise in car manufacturing. The collaboration 
				is the result of the Genesis Prada concept which was introduced 
				at the Seoul Motor Show in 2009.<br>
				<br>
				“The Middle East is a very strategically important market for 
				Hyundai, having been here for over 28 years,” says Mike Lee, 
				marketing manager, Hyundai Middle East Regional HQ. “The fact 
				that this is only the second market globally to receive this 
				limited edition vehicle underlines our commitment to the market. 
				Joining forces with Prada, a world-renowned fashion house, lends 
				a distinct air of luxury to the Genesis Prada and underscores 
				the importance of this award winning vehicle for Hyundai.”<br>
				<br>
				The joint project of Genesis Prada is the result of both 
				companies’ approach to create a highly unique version of the 
				Genesis model, addressed to the most sophisticated clientele. In 
				particular, the project reflects Hyundai’s new marketing 
				direction – “New Thinking. New Possibilities.” – which aims to 
				provide customers with new experiences and values beyond their 
				expectations. This matches the brand image of Prada, which also 
				balances tradition and innovation.<br>
				<br>
				Explaining the design cues that make the Genesis Prada a 
				distinguished vehicle, Francesco Longanesi Cattani Director of 
				External Relations, Prada says: “We felt it important to make 
				Genesis Prada owners not only see, but also feel the difference 
				when inside the car. From the outside-in, the car looks in a 
				class of its own, with special paint and badges. The interior 
				also gets special treatment, with signature Prada Saffiano 
				leather used to give it an air of luxury and sophistication.”
				<br>
				<br>
				<b>Tracing its roots</b><br>
				Genesis Prada is based on the 2012 Genesis, which was launched 
				in February 2011. The car, which won the prestigious 2009 North 
				American Car of the Year award, is powered by Hyundai’s Tau V8 
				engine, which has been chosen as one of Ward’s 10 Best Engines 
				for three consecutive years since 2009. The 2012 Genesis’s Tau 
				4.6 MPI engine produces 366ps at 6,500rpm, and 44.8kg/m of 
				torque at 3,500rpm mated to an eight-speed automatic 
				transmission that allows drivers to enjoy the car’s power and 
				driving dynamics to their fullest extent.<br>
				<br>
				Genesis Prada comes in three special colours – Black Nero, Blue 
				Baltico and Brown Moro – that are applied with a three-layer 
				coat technology with stereoscopic gold pearl. Dark chrome is 
				applied to the radiator grille, emblem and door handles to 
				reflect the unique palladium-coated buckles on Prada’s bags. The 
				innovations introduced by Prada include also 19-inch alloy 
				wheels and an outer antenna with a wedge shape. For the 
				interior, the dashboard and the upholstery have also been 
				revised and artisan-crafted in “saffiano” leather – Prada’s 
				signature top quality leather – to obtain a luxurious and 
				sophisticated finish.<br>
				<br>
				Genesis Prada will be produced by customised personal orders, 
				while the limited Genesis Prada badge and GP500 emblem will 
				emphasise the scarcity of the car, limited to a production run 
				of just 1,200 units globally. Hyundai, together with Prada, will 
				carry out exclusive VIP marketing activities to provide 
				customers with ultimate satisfaction and convenience. Initially 
				offered in limited numbers in the Korean market, this is the 
				first time that the Hyundai Genesis Prada is being exported to 
				the Middle East, and will be followed by other Asian countries 
				in the near future.&#8194;<br>
