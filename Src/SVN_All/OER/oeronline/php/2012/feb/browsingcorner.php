				Isaacson’s authorised biography captures the highs and lows, 
				strengths and foibles of Steve Jobs, presenting him as human as 
				anyone else. An OER review<br>
				<br>
				As Walter Isaacson says in his incisive biography, Steve Jobs 
				had the uncanny ability to compel others to do his bidding. As a 
				child, he made his parents sell their house and buy another one 
				that was beyond their means so he could qualify to enrol in a 
				better school. As an adult, he drove his silver Mercedes at the 
				speed of thought and parked it straddling spaces reserved for 
				disabled drivers: the normal rules were for ordinary mortals. 
				When first diagnosed with pancreatic cancer, he refused surgery 
				and thought he could zap the malignancy by applying mental 
				force, backed up – since this was ditzy, faddish California – by 
				a regime of “organic herbs, juice fasts and frequent bowel 
				cleansings”.<br>
				<br>
				At his best, Jobs mimicked the serene demeanour of a Buddhist 
				priest, just as the white or silver gadgets he sold exemplified 
				a style that Isaacson calls “techno-Zen”. He negotiated 
				multimillion dollar deals while walking barefoot through the 
				countryside and bought himself a mansion in which he had to sit 
				cross-legged on the floor because he disapproved of possessions 
				and had theoretical objections to furniture. But at his worst, 
				as a colleague said, he resembled Rasputin. He screamed on 
				subordinates, fired them abruptly, and his vindictive temper 
				worked like a guided missile. During a dispute with Google, he 
				declared that he intended “to go to thermonuclear war on this”.<br>
				<br>
				<b>The counterculture effect<br>
				</b>Isaacson catches these contradictions with unerring skill 
				and partly justifies them by placing Jobs at the awkward 
				intersection between two generations with opposing creeds. Born 
				in 1955, he belonged to the bratty clan of baby-boomers who 
				rebelled against the suburban conformity of their parents. He 
				dropped out of college and simultaneously dropped acid, visited 
				Indian gurus and idolised Bob Dylan. But although he emerged 
				from the counterculture, he became an emblem of corporate 
				culture and even commissioned Issey Miyake to design a uniform 
				for Apple employees.<br>
				<br>
				He may have disdained material encumbrances, but his business 
				relied on inciting consumerist desires in others. The revolution 
				he dreamed of in the days of flower power turned out to be 
				electronic, not political. The former hippie sold his iPods and 
				iPhones to hipsters who have no interest in changing the world 
				but simply want to enjoy it as they groove to their pocket 
				library of iTunes and loll on digital clouds cyber-chatting with 
				friends they have never actually met. The biography is honest 
				and therefore often harsh, but it confers on Jobs a kind of 
				tragic desperation when, with the onset of cancer, he discovers 
				that his infinitely looped and ingenious mind is housed in a 
				fallible body. So cerebral that he disliked eating and preferred 
				to starve himself, he was eventually eaten alive by his greedy 
				tumour.&#8194;<br>
