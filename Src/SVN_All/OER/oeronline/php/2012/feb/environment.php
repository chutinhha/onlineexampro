						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">
						Revitalising heritage</font></b><p align="justify" style="margin: 3px 10px"><b><font face="Verdana" size="2">
                		<br>
						HSBC Oman’s Frankincense Research Project in 
						collaboration with Environment Society of Oman seeks to 
						determine the right frequency of tapping frankincense 
						trees<br>
                </font></b>
				<font face="Verdana" size="1">
				<br>
                		Frankincense trees are not only important for the 
						protection of the environment but are also an integral 
						part of the Omani heritage. Preliminary findings 
						indicate that the manner in which trees are tapped to 
						harvest frankincense can lead to degeneration beyond 
						repair. HSBC Oman’s Frankincense Research Project in 
						association with Environment Society of Oman (ESO) aims 
						to determine the right frequency of cutting to retrieve 
						a good yield without harming the trees. The study will 
						also pinpoint the potential impacts of climate change on 
						the growth pattern of Frankincense trees.<br>
						<br>
						Concrete findings and recommendations to ensure the 
						sustainability of frankincense farming, a primary source 
						of Dhofar’s livelihood, will then be disseminated to 
						farmers, relevant government authorities including the 
						Ministry of Agriculture and Ministry of Environment and 
						Climate Affairs and the local community for 
						implementation. Educational programmes in school will 
						also be introduced to spread long-term awareness amongst 
						youth to ingrain a greener mindset.<br>
						<b><br>
						Three goals </b><br>
						The project aims to engender three major benefits, the 
						first is to revitalise the generations-old business of 
						frankincense farming and the second is to minimise urban 
						sprawl in Dhofar and finally to engage and build 
						capacities of the local farmers. Initiated in February 
						2010, the project is funded by HSBC and executed by the 
						Environment Society of Oman (ESO), and it aims to 
						successfully promote environmental awareness and to 
						support unique local attributes to Oman.<br>
						<br>
						The ESO’s lead researcher of the project, Dr Mohsin Al 
						Amri has been conducting quantitative fieldwork which 
						includes collecting, recording and analysing data since 
						the start of the project and will continue for the next 
						four years. To date, 180 were sampled in four identified 
						areas in the Governorate of Dhofar, which were then 
						classified according to the circumference of the trunk 
						and tagged. A part-time technician was also trained 
						directly by Dr Al Amri, to build local capacities and 
						highlight the importance of safeguarding Frankincense 
						farming among the community. Three weather stations were 
						also installed to pinpoint the potential impacts of 
						climate change.<br>
						<br>
						The frankincense produced by each tree is weighed and 
						the data collected is stored in the project’s database. 
						At the end of the project, a maximum yield can be 
						estimated for each of the four sites. All the parameters 
						collected over the first year of research represent the 
						baseline data of the project. The data collected in the 
						subsequent years will be benchmarked against this data 
						to identify trends in terms of abundance, health and 
						yield. At the end of the four-year research, the project 
						will attempt to establish a correlation between the 
						different characteristics of the frankincense trees and 
						their yield.&#8194;<br>
