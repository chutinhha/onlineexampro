				<font face="Verdana" size="4" color="#C00000">15 AND COUNTING </font></b><p align="justify" class="style13">
				Oman Economic Review completes 15 years in 2012. What started 
				out as a dream of empowering businesses and educating readers 
				about the achievements of entrepreneurs in 1997, has ended up 
				creating publication history over the years. Oman Economic 
				Review has numerous milestones to its credit – it was the first 
				magazine to start ranking the largest listed companies in the 
				Sultanate as The OER Top 20 a decade ago; it pionered surveys 
				like The Most Powerful Women in Business; The Best Banks in Oman 
				and The Fastest Growing Companies.<br>
&nbsp;<br>
				The brand has transcended the publication space by going into 
				events like OER CEO Golf; The Oman Debate and Oman Green Awards. 
				It gives me great pride to state that Oman Economic Review is 
				recognised as one of the leading magazines in the GCC region. 
				Our achievements are the result of our credo of constant 
				learning. <br>
				<br>
				As we commence our onward journey, we are taking another 
				momentous step by changing our masthead from Oman Economic 
				Review to OER. The look and feel of the magazine also undergoes 
				a metamorphosis. In an age where social networks, short messages 
				and tweets are the order of the day, OER’s reader-friendly 
				format connects instantaneously. While the magazine may have 
				taken this bold step, our promise of giving our readers cutting 
				edge and profound articles remains intact.<br>
				<br>
				Visvas Paul D Karra, Assistant Editor, who has been tracking the 
				Budget for the fourth year in a row says, “The focus for 2012 is 
				on job creation, education, training and health, which is good 
				news for the citizens. However, the private sector and 
				especially the SME segment would have been in a comfortable zone 
				had there been some concrete proposals for them.” You can read 
				his pointed analyses of the main highlights of Budget 2012 in 
				this edition’s cover story.<br>
				<br>
				We look forward to your valued feedback and continuing support 
				in the years ahead. <br>
