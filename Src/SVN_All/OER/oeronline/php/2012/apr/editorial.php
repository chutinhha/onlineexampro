				The letter ‘D’ connoting downgrade became the bugbear of banks 
				and bankers around the world in 2011. The big three global 
				rating agencies were on a tear downgrading banks in Europe, US 
				and Asia. Standard &amp; Poor’s (S&amp;P), lowered the ratings of close 
				to a dozen Spanish financial institutions including the 
				country’s two biggest banks – Banco Santander and Banco Bilbao 
				Vizcaya Argentaria. Moody’s downgraded Russia’s banking system 
				from stable to negative and the long term ratings of Bank of 
				America were brought down two notches. Fitch Ratings painted a 
				negative outlook on Italy’s five largest banks. The world’s 
				largest bank in terms of assets, the France based BNP Paribas 
				also saw its ratings being cut by a notch. <br>
				<br>
				&#8207;Countrys fared no better with the S&amp;P downgrading the US 
				economy from its prized AAA status to AA+. The Italian 
				government’s debt rating was cut from A+/A-1+ to A/A-1. Spain, 
				Ireland, Greece, Portugal and Cyprus were the other countries 
				with a credit rating downgrade. <br>
				<br>
				&#8207;Compared to this, Oman was an oasis of stability and calm. 
				Driven by higher oil revenue and prudent macro-economic policies 
				adopted by the government, the domestic economic scenario 
				witnessed considerable improvement in 2011. Higher than 
				forecasted oil revenue resulted in a budget surplus for the year 
				despite an increase in government spending. Inflation has 
				remained at acceptable levels and IMF forecasts inflation to 
				moderate to 3.3 per cent in 2012. <br>
				<br>
				&#8207;The banking industry in the Sultanate rode this crest and came 
				up with a consistent performance. Most of the banks reported 
				positive numbers in 2011 and the OER-GBCM Best Banks survey 
				underscores this point. Our cover story package profiles the 
				seven local banks, foreign banks, Bank Nizwa -- the first 
				Islamic Bank in the country and NBFCs. Read it to get a 
				comprehensive view of the sector along with the winners of this 
				year’s ranking.<br>
