						<td valign="top">
						<p align="justify" style="margin: 3px 10px">
						<span class="style27"><strong>‘Need for a National 
						Talent Development Plan’</strong></span><p class="style15">
				<strong><span class="style14"><br>
&nbsp;</span></strong><p align="center" class="style13">
				<img border="0" src="images/event-apr-12-1.jpg" style="border: 1px solid #C0C0C0" width="400" height="266"><p align="justify" class="style13">
						<br>Omar Ahmed Qatan, CEO, Oman Oil Marketing Company 
						says Oman needs a National Talent Development Plan which 
						will align all associated investments towards achieving 
						the national strategic objectives. Qatan will take part 
						as a panelist in Oman Forum – Building National Talent. 
						Excerpts from an interview<br>
						<br>
						<b>AWhat have been the key achievements of Oman in 
						building national talent? <br>
						</b>Oman has shown an exemplary commitment towards the 
						development of local talent at the highest level. The 
						Sultanate has been developing institutional capacities 
						in the education and academic systems. Substantial 
						investments have been made in developing 
						education/academic systems by improving the quality of 
						the lecturers/teachers, infrastructure and syllabus. All 
						these actions have been aimed at continuous alignment 
						and compliance with the international standards. 
						Knowledge transfer has been enabled through exposing 
						national talent to experienced international expertise. 
						The nationals have been provided development 
						opportunities in all sectors to practice their skills 
						and exhibit their talent by undertaking challenging 
						projects, roles and responsibilities.<br>
						<br>
						We have been quite successful in developing a balanced 
						and supportive regulatory system that gives immense 
						weightage to support the development of national talent 
						in their career and professional training. The United 
						Nations has also recognised Oman’s achievement in the 
						development of human capital.<br>
						<br>
						<b>What is your advice to the corporate leaders in 
						maintaining a healthy balance between profit orientation 
						and developing national talent? <br>
						</b>It’s always important to align the process of 
						talents development with organisational objectives. 
						Attracting passionate individuals and hiring them in 
						their areas of specialty and interest makes that 
						alignment more effective. In OOMCO, we have the higher 
						education policy which entitles the staff to receive 
						funding from the company to undertake a higher education 
						course as long as the field of study is related to 
						his/her work.<br>
						<br>
						<b>What kind of a role do you expect expatriates to play 
						in Oman say post 2015 or so? <br>
						</b>Professional expatriates have played and will 
						continue to play an important role in the knowledge 
						transfer by sharing their international experience with 
						the local talent. They have helped in developing Oman in 
						various fields, and as the nation expand beyond its 
						comfort zone we need to accelerate our learning curve 
						through hiring expatriates directly or engaging 
						international firms, in order to make faster transition 
						and maintain our competitiveness in the global market.<br>
						<br>
						<b>In which functional areas do you think there is a 
						significant national talent gap? <br>
						</b>The gaps are more prominent in areas such as 
						financial accounting, information technology and some 
						branches of engineering where there is a heavy reliance 
						on skilled expatriate labour. The supply of skilled 
						national talent is not good enough to meet the high 
						demand of the market. <br>
						<br>
						<b>What could be done to establish a sustainable and 
						effective public-private partnership towards building 
						national talent? </b><br>
						The country needs a National Talent Development Plan -- 
						a plan which can consolidate the initiatives of all 
						stakeholders and align all associated investments 
						towards achieving the national strategic objectives. 
						This plan can only be achieved and brought to reality 
						through the establishment of a body that overlooks and 
						orchestrates the talent development process and reports 
						to the highest authority in the country. <p align="justify" style="margin: 3px 10px">&nbsp;</td>
