						<font face="Verdana" size="4" color="#C00000">
						Heralding a new era of banking</font></b><p align="justify" style="margin: 3px 10px">
						<b>
						<font face="Verdana" size="2">
                		<br>
						Bank Nizwa, Oman’s first dedicated Islamic bank, will 
						offer an entire portfolio of commercial banking services </font></b>
						<p align="justify" style="margin: 3px 10px">
						&nbsp;<p align="center" style="margin: 3px 10px">
						<img border="0" src="images/inthenews-apr-12-1.jpg" width="500" height="333"><p align="justify" style="margin: 3px 10px">
				<font face="Verdana" size="1">
				<br>
				Bank Nizwa has recently launched its brand-mark moving towards 
				the establishment of the first Islamic bank within the country. 
				The bank will be Oman’s first dedicated Islamic bank, and all 
				products and services offered will be Shari’a-compliant. Bank 
				Nizwa will eventually offer an entire portfolio of commercial 
				banking services, in accordance with the license to be issued by 
				the Central Bank of Oman (CBO) and the Banking Law promulgated 
				by the Royal Decree No. 114/2000. Further, the CBO will soon be 
				issuing an Islamic Banking Regulatory Framework (IBRF) for Oman 
				and Bank Nizwa will operate in accordance with this IBRF once it 
				comes into force. <br>
				<br>
				The formation of Bank Nizwa came about with the CBO granting its 
				initial approval to the licensing of the bank to Sheikh Saud bin 
				Ali Al Khalili. Sheikh Saud along with 91 other founder 
				shareholders appointed a founding committee to represent them, 
				and to help manage the establishment of the bank as a licensed 
				Shari’a-compliant commercial bank. The founding committee 
				consists of five individuals including the chairman of the 
				founding committee, Ahmed bin Saif Al-Rawahi. The founding 
				committee of the bank, together with its appointed consultants, 
				is undertaking the development of an operating model for the 
				bank, the procurement of the IT infrastructure to support the 
				bank’s products and back-office functions, and the creation of 
				the internal polices and procedures required for the opening and 
				operation of the bank. Key members of the senior management team 
				have been identified and some of them were formally appointed. 
				Bank Nizwa is also in the process of obtaining approval for 
				setting up its head office and the first three branches to be 
				located in Muscat , Nizwa and Sohar.<br>
&nbsp;</font><p align="center" style="margin: 3px 10px">
						<img border="0" src="images/inthenews-apr-12-2.jpg" width="500" height="333"><p align="justify" style="margin: 3px 10px">
				<font face="Verdana" size="1">
				<br>
				‘We have undertaken a detailed feasibility study for 
				establishing the bank and have drawn up a business plan in 
				conjunction with an internationally reputed consultancy firm. 
				The study reveals that there is substantial unmet demand for 
				Shari’a-compliant products in Oman. This together with the 
				attractive economic fundamentals of Oman and the growth in the 
				Omani banking market, highlights the growth opportunity for Bank 
				Nizwa,’ said Ahmed Al-Rawahi.<br>
				<br>
				The market for Islamic finance has grown rapidly over the last 
				few years with the global value of Shari’a-compliant assets 
				reaching an estimated value of $1tn in 2010. Customers across 
				the globe have shown an increasing propensity to adopt Islamic 
				banking as their preferred mode of financing as faith-based 
				attitudes coupled with viable options from Islamic banking have 
				reinforced greater adherence to Shari’a in economic matters. <br>
