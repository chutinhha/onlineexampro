				<img border="0" src="images/browsingcorner-apr-12-1.jpg" align="right" style="border: 1px solid #C0C0C0" hspace="12" vspace="3">The 
				Rules Of Wealth by Richard Templar intends to shape one’s 
				financial behaviour without resorting to abstract business 
				jargon. An OER review <br>
				<br>
				The Rules Of Wealth delivers sterling ideas for becoming 
				wealthy. The book is overflowing with critically important 
				subjects centered on getting rich. The book aims to distill the 
				mindset and strategies of the super-wealthy into an 
				easy-to-follow set of rules that might lead anyone to personal 
				prosperity. There are one hundred rules in total, and these are 
				split across five sections; Thinking Wealthy, Getting Wealthy, 
				Get Even Wealthier, Staying Wealthy, Sharing Your Wealth. <br>
				<br>
				&#8207;The author enlightens the reader on business subjects that are 
				highly useful. The best thing about the book is that you don’t 
				have to sift through page after page of written text to finally 
				get to the main point. Templar cuts to the core on every topic. 
				As a result, you don’t waste time on insignificant details. The 
				author also simplifies difficult concepts in the business world.
				<br>
				<br>
				<b>&#8207;Light read <br>
				</b>&#8207;The book’s strength is its breadth rather than its depth - 
				the rules are intended to shape one’s financial behaviour 
				without delving into the minutiae of its recommendations. <br>
				<br>
				&#8207;The writing is fairly easy to read and there is no unnecessary 
				use of technical language which might intimidate the amateur 
				economist. On the whole, Templar comes across as a well-grounded 
				and self-effacing individual, his writing is personable and he 
				acts as a wholly amiable narrator.<br>
				<br>
				&#8207;One of the small drawbacks of the book is its lack of a defined 
				target audience. Most financial development books are aimed at 
				either the individual, or at the entrepreneur. In trying to 
				appeal to the widest possible audience the book does neither 
				satisfactorily. Indeed, there are times when this causes an 
				internal dissonance. For example, on the matter of loans. While 
				for an individual, personal loans are normally financially 
				unwise, for an entrepreneur a business loan is an ideal form of 
				leveraging. While it’s possible to sit these two viewpoints 
				side-by-side it creates a rather jumbled and somewhat imprecise 
				feel.<br>
				&#8207;The information contained in The Rules of Wealth is nothing one 
				couldn’t find on a good financial blog; but the attempt to 
				codify a set of wealth rules is admirable. Templar’s moral code 
				provides a refreshing backdrop to the financial advice as does 
				his endorsement of hard work. The book’s simplicity is probably 
				one of its most salient selling points, the rules that Templar 
				has gathered together, is unlikely to make anyone extremely 
				wealthy, but it certainly provide some basic financial pointers 
				to keep your money life in good order.<br>
