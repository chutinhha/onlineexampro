				<b>Social conscience<br>
				</b><br>
				Oman International Bank extended its support to a number of 
				social and community driven initiatives in 2011 proving its 
				strong connect with society<br>
				<br>
				Oman International Bank (OIB) offers a comprehensive range of 
				financial &#8207;products and services to the government, corporate 
				clientele, retail sector and to &#8207;the public. The Bank has a 
				network of 85 branches in the Sultanate of Oman. In &#8207;India the 
				bank has two branches and in Pakistan it has three branches. OIB 
				has got an in principle approval from the Central Bank of Oman 
				for a merger with HSBC’s Oman operations. The combined entity 
				would have the scale to be a leading player in the fast growing 
				Omani banking market. <br>
				<br>
				&#8207;<b>Financial performance <br>
				</b>&#8207;OIB’s total interest income achieved for the year 2011 was 
				RO36.83mn compared &#8207;to RO36.60mn for the previous year. Interest 
				expense reduced to RO6.43mn &#8207;against RO6.76mn for the 
				corresponding period last year. Net interest &#8207;income was 
				RO30.399mn compared to RO29.83mn last year. Other &#8207;operating 
				income increased to RO11.75mn against RO10.25mn for the 
				&#8207;previous year. Thus, the total income was higher at RO42.15mn 
				compared &#8207;to RO40.09mn for last year. The bank achieved a net 
				profit of RO16.51mn for the year 2011, compared with RO17.58mn 
				for the year 2010. &#8207;The bank’s strategy in India continued to 
				focus on recovery of advances. Indian &#8207;operations achieved a net 
				profit of RO1.75mn on the back of a major &#8207;recovery of a bad 
				debt in 2011. OIB is closely monitoring its operations in 
				Pakistan &#8207;to improve results.<br>
				<br>
				&#8207;<b>Corporate governance</b><br>
				&#8207;The bank is firmly committed to the highest standards of 
				corporate &#8207;governance and has complied with CMA, various 
				regulatory bodies and &#8207;international best practice. OIB has 
				chosen Protiviti to implement the Governance, Risk and 
				Compliance (GRC) portal to enhance its internal audit, financial 
				controls management, &#8207;enterprise risk management, IT governance 
				and compliance functions. &#8207;Protiviti’s GRC Portal is an 
				integrated platform that facilitates a common risk &#8207;management 
				approach, enabling a sustainable, repeatable and cost-effective 
				&#8207;governance and compliance programme. The Internal Audit &#8207;portal 
				was successfully completed during 2011. <br>
				<br>
				&#8207;<b>HR development <br>
				</b>&#8207;The human resource strategies of the bank aims to foster 
				and enrich &#8207;the staff competence and experience so as to improve 
				productivity. &#8207;The bank’s recruitment policy provides equal 
				competitive opportunities for all to select the most qualified 
				Omanis. As part of bank’s commitment, the Omanisation ratio 
				stands at 91 per cent which exceeds the Omanisation ratio set by 
				the Central Bank of Oman.<br>
				<br>
				&#8207;&#8207;In 2011, the bank offered 260 training courses in various 
				centres, &#8207;in-house, locally within the Sultanate and abroad, 
				involving a total of 2,573 &#8207;employees. The bank also received a 
				special recognition from the College &#8207;of Banking Studies and 
				Finance for the highest rate of participation of any bank &#8207;in 
				Oman in their courses. &#8207;OIB is currently in the process of 
				installing 300 new state-of-the-art ATM machines. &#8207;Currently 212 
				machines have been imported from a European vendor and the &#8207;105 
				existing ATMs at its branches will be replaced with these new 
				machines in the second quarter of 2012. <br>
