				&nbsp;<b>Best Banks Ranking Methodology</b><br>
				<br>
				We have considered the growth and the sustainability parameters 
				for the ranking of commercial banks in Oman. The banks ranking 
				model was done among the leading local banks which include Bank 
				Muscat, National Bank of Oman, Oman Arab Bank, Bank Dhofar, Oman 
				international Bank, Ahli Bank and Bank Sohar. We have taken the 
				period from 2008 to 2011 to rank the banks on the set parameters 
				for growth and sustainability. For the current year ranking 
				process, the newly established Bank Sohar has been included with 
				the completion of four years of operations along with the 
				availability of data required. During 2010, AhliBank became part 
				of the ranking process.<br>
				<br>
				The data for the survey was sourced from the published financial 
				statements (audited) of the banks, CBO reports and other 
				published reports. Ranking based on growth is done by 
				calculating the compounded annual growth rate (CAGR) of gross 
				loans, customer deposits, fee income, operating profit, networth 
				and net profit during the period from 2008 to 2011. In our 
				ranking model, the operating profit is calculated by subtracting 
				total operating income with the total expenses (excluding 
				depreciation costs). Ranking for sustainability factor was done 
				by calculating the four year average of three key factors like 
				asset quality, productivity and efficiency. <br>
				<br>
				Asset quality was ranked based on non performing assets to the 
				gross loans (NPA/GL) ratio and the overall loan provisions to 
				non performing assets (provision coverage ratio). The 
				productivity is ranked based on the operating profit of the bank 
				to its total number of branches and number of employees. In 
				conclusion, the efficiency factor considers net interest margins 
				(net interest income/average interest bearing assets), cost to 
				income ratio, return on average assets and return on average 
				equity. For the calculation of cost to income ratio, we have 
				excluded the depreciation expenses. <br>
				<br>
				Verification <br>
				The results of the survey has been verified by Abu Timam Grant 
				Thornton, one of Oman’s leading accounting, tax and business 
				advisory firms dedicated to serving the needs of privately held 
				businesses and public interest entities. The firm offers a full 
				range of assurance, corporate finance, business advisory and tax 
				services. As a member firm of Grant Thornton International, Abu 
				Timam it has access to member and correspondent firms in over 
				600 offices in more than 100 countries. Abu Timam Grant Thornton 
				was established in 1995 and its managing partner is Nasser Al 
				Mugheiry, a professionally qualified FCCA. He has more than 25 
				years of extensive experience in statutory audits of various 
				industries, internal audits, advisory services. <br>
				<br>
				<b>Off the blocks </b><br>
				<br>
				<br>
				Sheikh Ahmed Saif Al-Rawahi, Chairman of Founding Committee, 
				Bank Nizwa shares his views on the challenges and opportunities 
				of being the first Islamic bank in the Sultanate and how 
				competition will help in educating the market about Islamic 
				finance in an interview with OER<br>
				<br>
				<br>
				Bank Nizwa has the distinction of being the first Islamic bank 
				in the Sultanate of Oman. Is this an opportunity or a challenge? 
				Please give some reasons.<br>
				<br>
				The opportunity is to be a great bank and the first Islamic bank 
				in Oman. We are bringing Islamic banking solutions to the people 
				in the Sultanate,where previously this wasn’t a choice. We would 
				like to take advantage of being the first mover. This is good 
				for customers and believing that we will do this right and well, 
				it will also be good for the bank and its shareholders.<br>
				<br>
				The challenges are broadly two fold: We are starting a new bank, 
				and also bringing a new form of banking to the market. By way of 
				example, starting a bank from scratch means that everything has 
				to be built and developed which requires time, resources and a 
				lot of dedication. However, what this means is that we have a 
				chance to build everything just the way it should be, as opposed 
				to being constrained by years of legacy systems and practices. 
				It also means that our people, the most important part of any 
				business, are all new to the organisation and thus motivated and 
				passionate about this new venture, and committed to the vision 
				of the business. We are recruiting experienced bankers in the 
				senior roles, many of them experienced ‘Islamic bankers’ and a 
				good number of Omanis. Across other levels we are recruiting a 
				mix of experienced bankers and other ‘great people’ who are 
				committed to building this ‘great bank’, and are all 
				collectively passionate about building and growing Oman’s first 
				dedicated Islamic bank.<br>
				<br>
				Other challenges include: consumer and market awareness ; 
				developing a distribution footprint and capability that reaches 
				as many customers as possible, as quickly as we can; creating, 
				developing and launching a full range of Shari’a-compliant 
				products to meet the needs of all our customers, and building an 
				integrated top class team of people to execute and manage our 
				operations and deliver top class service to our customers. We 
				know what the challenges are, and are ensuring we bring in the 
				best people and work with the best partners and suppliers. We 
				will meet these challenges head-on and create a great bank.<br>
				<br>
				<b>How much of ground work has been done by the founding 
				committee in terms of human resources, composition of the 
				Shari’a board, systems and processes to get the bank up and 
				running as an when the Central Bank of Oman gives the formal go 
				ahead?</b><br>
				A vast amount of ground work has been done. This has been led by 
				our founding committee, working alongside professional advisors 
				and consultants, and more recently our incoming management team.<br>
				<br>
				In terms of human resources we have received thousands of CVs 
				and have undertaken literally hundreds of interviews. We have 
				identified the entire senior management team for the bank, 
				covering all the key functions, and those who have not yet 
				started will be joining the bank over the coming weeks.<br>
				<br>
				We have identified our Shari’a board, and are finalising the 
				formalities of appointing the members. Once the board members 
				are formally appointed we will be announcing their names to the 
				market. I can confirm that we intend to have four scholars on 
				the Shari’a board of which two are Gulf-based internationally 
				renowned scholars, who have many years of experience in a vast 
				range of Islamic banks, and the other two are locally based 
				scholars. We consider this to an ideal combination of Islamic 
				banking and Omani experience and credibility.<br>
				<br>
				With regard to systems and processes, we are working with 
				specialist consultants and have already developed our policies 
				and high level procedures. The detailed procedures and processes 
				are being developed in parallel with the development of our 
				products and roll out of our core banking system. We have 
				already announced that we are using Path Solutions iMal system, 
				and this work is progressing at a good pace. The other major 
				component of the operational build is branches. We have secured 
				the sites for our first three branches, which will be in Muscat, 
				Nizwa and Sohar, and we are in the process of fitting these out. 
				We have identified other strategic locations across the 
				Sultanate, and will be opening further branches in due course.<br>
				<br>
				<b>What is the capital base of Bank Nizwa, both authorised and 
				the amount that has been raised currently? The bank plans to 
				float an IPO soon; how much stake will the bank be divesting 
				through the public offering? Can you also share a few details 
				about the expected timeline, the lead manager and the money that 
				you expect to raise through the IPO?</b><br>
				The total authorised share capital of the bank is RO300mn, of 
				which the paid up capital before the public offering of the 
				shares will be RO90mn. On the IPO the bank intends to raise 
				further capital of RO60mn leading to a total paid-up capital of 
				OR150mn. <br>
				<br>
				The IPO is expected to be launched during Q2 of 2012 led by the 
				IPO lead manager, Oman Arab Bank. The offer will be open to both 
				Omanis and non-Omanis.<br>
				<br>
				Most banks in the Sultanate are gearing to tap the opportunity 
				presented by the opening up of Islamic banking in Oman? Given 
				such a scenario what makes you confident that customers will 
				choose to bank with Bank Nizwa and not with other established 
				banks.<br>
				<br>
				Ironically, we positively welcome other entrants into the 
				Islamic banking market. Islamic banking is new to Oman, and one 
				of our challenges is creating awareness in the market, including 
				consumers and influencers, such as the media and professional 
				advisors, about how Islamic finance principles and concepts 
				under Islamic Shari’a work in Islamic banks. For just one bank 
				this would be a huge challenge, but with many players entering 
				soon in the market we will collectively help to educate the 
				marketplace, which is great for the banks and customers. Right 
				now our interests are fully aligned, only when all customers 
				understand what Islamic banking is and want to use it, do we 
				become competitors. It has always been said that competition is 
				a good thing and it keeps people and businesses on their toes. 
				At Bank Nizwa we are not relying on customers coming to us 
				because we are an Islamic bank. Our objective is to be a great 
				bank, which is also Islamic. To ensure we become a great bank we 
				are committed to high Islamic and ethical standards, business 
				excellence, appropriate use of innovative technology and 
				customer-centric service excellence.<br>
				<br>
				We cannot be confident that customers will chose to bank with 
				Bank Nizwa, it is their choice, but we are going to do 
				everything we can to prove to customers that we are the right 
				bank for their faith and money. In the end it is the customers 
				who will decide if we have got it right. <br>
				<br>
				<b>BOX<br>
				<br>
				Dr Jamil El Jaroudi appointed as CEO &#8207;<br>
				</b><br>
				Following Central Bank of Oman’s formal approval Bank Nizwa, 
				Oman’s first Islamic bank, announced the appointment of Dr Jamil 
				El Jaroudi as its CEO. Ahmed Saif Al Rawahi, chairman of the 
				Bank Nizwa founding committee, commented, “Until we had formal 
				approval from the CBO we couldn’t go public with details of our 
				chosen CEO, but now we can and it gives me great pleasure to 
				announce the appointment of Jaroudi as Bank Nizwa’s first CEO. 
				Jamil brings many years of commercial experience, but even more 
				importantly has strong credentials in both Islamic banking and 
				banking start-ups.”<br>
				<br>
				&#8207;Jaroudi has over 40 years business experience, and joins Bank 
				Nizwa from Elaf Bank in Bahrain, where he was CEO. Jaroudi 
				started his working life as a consultant with Booz Allen &amp; 
				Hamilton, in the UAE and the Kingdom Of Saudi Arabia, after 
				which he held various senior executive roles with Al Mawarid 
				Group, Saudi Arabia. In the mid-1990’s he co-established the 
				Middle East Capital Group in Lebanon, the region’s first 
				merchant bank with a pan-Arab focus. Subsequent to this Jaroudi 
				held senior positions with DMI Trust, Islamic Investment Company 
				of the Gulf and Shamil Bank, and was involved in the 
				establishment of Arab Finance Bank in Lebanon and most recently 
				Elaf Bank in Bahrain.<br>
				<br>
				&#8207;Jaroudi holds an MBA from the Ivy League school Columbia 
				University, and a PhD in Finance from Kellogg School, 
				Northwestern University, USA. Among other activities for NGOs 
				and charitable organisations, he is a Board member of the Beirut 
				Islamic University, and a member of the Board of Trustees of the 
				Lebanese American University Institute of Family Businesses. 
				Just recently, Jaroudi was nominated on the governing council of 
				INCEIF, the Global University of Islamic Finance in Malaysia.<br>
