						<td valign="top">
						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">
						A robust showing</font></b><p align="justify" class="style13" dir="ltr">
				<br>
				<b>Healthy competition </b><br>
				<br>
				&#8207;Abdul Kader Askalan, CEO, Oman Arab Bank feels that Islamic 
				banking presents an opportunity and a challenge for the banking 
				sector in the Sultanate in an interview with OER <br>
				<br>
				&#8207;<b>Oman Arab Bank (OAB) has come up with a good performance in 
				2011. What are the factors that enabled the Bank to do well in 
				the year gone by?</b><br>
				&#8207;Overall our performance has been very good -- our profit in 
				2011 is equal to the previous year and this has been achieved 
				despite the increase in our expenses, due to the increase in 
				salaries and manpower costs. As with other banks most of OAB’s 
				profits come from interest and the rest of the profit accrues 
				from trade and project finance, commissions, contract related 
				activities, guarantees and other things. The bank has been able 
				to grow its business in all these areas and these find a 
				reflection in the numbers. <br>
				<br>
				&#8207;<b>OAB has always been very strong in the project financing 
				arena. Which were the major projects that you financed in 2011?
				</b><br>
				&#8207;We are financing a lot of projects both small and big, some of 
				these projects are in Duqm and Sohar. We finance projects 
				individually and with other banks as syndications or 
				consortiums. <br>
				&#8207;<br>
				<b>Are there a lot of infrastructure projects taking place in 
				Oman?<br>
				</b>&#8207;Yes, there are a number of projects that are coming up in 
				Oman like the expansion of Raysut Cement, projects in the 
				various freezones, the electricity projects, ongoing projects in 
				Duqm and we are looking at all of these as prospective business 
				opportunities. <br>
				&#8207;Apart from project finance the bank has been focussing on the 
				retail side aggressively in the last few years.<br>
				<br>
				<b>How has your performance been in retail banking? <br>
				</b>&#8207;In retail banking we have been doing as planned, we are not 
				concentrating only on the retail business but anyway, our retail 
				business is much better than before. We are offering new 
				products, but I am very keen that we should not exceed the 
				prescribed limits for personal loans as it is dangerous for 
				borrowers. <br>
				<br>
				<b>Central Bank of Oman (CBO) has permitted Islamic banking in 
				Oman, what impact will this have on the Sultanate’s banking 
				sector and what are OAB’s plans in this direction? <br>
				</b>We are looking at opening a window for Islamic services and 
				we are taking certain steps to achieve the requisite regulatory 
				procedures. The bank is currently training its staff who we will 
				be taking care of its Islamic service. We are waiting for the 
				laws to be issued by the CBO to commence our service. <br>
				&#8207;<br>
				<b>When do you expect Islamic banking regulations to be issued?<br>
				</b>&#8207;As the CBO has said the rules will be issued in the next 
				three months. As you are aware two banks have been given an 
				Islamic banking license. Other banks have been permitted to come 
				up with Islamic finance windows and all of us are waiting for 
				the regulations to be issued. <br>
				&#8207;<br>
				<b>What impact will Islamic finance have on the market?<br>
				</b>&#8207;It is a new system and is one of the additional products 
				that we can offer to our customers. As it is a new product or 
				system it is our duty to educate people about Islamic banking, 
				so that customers can judge the costs and the merit of these 
				products. Some customers may think that there is no cost 
				attached to such products, but they will discover that there is 
				a charge for such products. Once they realise that there is a 
				charge they may compare Islamic banking with commercial banking. 
				Once they realise that there is a certain additional cost on 
				Islamic banking, then the question is whether they will stay or 
				return to commercial banking? In the beginning most people would 
				go for Islamic banking but the service and cost implications 
				will decide whether they will stick to it or not. <br>
				&#8207;<br>
				<b>So are there challenges and opportunities that Islamic 
				banking will throw up in Oman?<br>
				</b>&#8207;Yes there are challenges that both the commercial banks and 
				Islamic banks will have to face. On the other hand it is good as 
				it will diversify our product base. Islamic banking is very 
				popular in most countries, both in the region and 
				internationally, but it all depends on how it is offered to the 
				customer. <br>
				<br>
				&#8207;<b>What were the challenges that you faced in 2011?<br>
				</b>&#8207;There were no extraordinary challenges. Our staff costs 
				grew and we needed to invest in improving and updating our 
				services for the customer. The bank has put in place new IT 
				systems, programmes, ATMs, cash deposit machines etc to comply 
				with the requirements of modern day banking and this has a cost 
				implication. <br>
				&#8207;OAB is constructing its new headquarters, when do you expect to 
				move in to the new location?<br>
				&#8207;We expect to move into our new building by May or June 2012 as 
				a lot of infrastructure work in terms of parking facilities, 
				roads etc still needs to be done. <br>
				&#8207;<br>
				<b>Can you tell us about OAB’s plans for FY 2012?<br>
				</b>&#8207;We have budgeted a growth of 10-12 per cent in 2012. Our 
				focus will be on project finance and on increasing our branch 
				network. Apart from this we also need to modernise our services 
				for customers. <br>
				&#8207;<br>
				<b>You mentioned about expanding your branch network, 
				specifically what are you looking at?<br>
				</b>&#8207;Every year we open two to three new branches. In 2011 we 
				opened three branches, this year we will be opening three or 
				more branches. Our aim is to have branches in most areas of the 
				Sultanate. We were the first bank to open a branch in Duqm, but 
				now we need to go to Musandam, Khasab etc. <br>
				&#8207;<br>
				<b>OAB has a strong presence in the portfolio management space, 
				what accounts for your success in this area? <br>
				</b>&#8207;Our investment group has a lot of experience in managing 
				portfolio’s. We are managing funds for the government and our 
				own funds. The funds that we are managing are not losing as much 
				as the securities markets. Thus we have been able to minimise 
				losses on the one hand and have made profits (at times) on the 
				other. <br>
				&#8207;<br>
				<b>Will the entry of new banks enhance competition in the 
				Sultanate’s market? <br>
				</b>&#8207;Things are going to become more challenging in terms of 
				competition. We hope that such intense competition gets reduced 
				as a result of the Banking Association that has been formed 
				recently. The association has representation from all banks -- I 
				have been elected as the chairman of the association and we have 
				formed a board of directors. The association will give us a 
				chance to negotiate and see as to how we can minimise it as 
				unfettered competition will destroy the banking sector. If a 
				bank has a policy of competing with another bank, it will impact 
				all the banks and we hope that this association will minimise 
				such instances. The association will discuss all the rules being 
				issued by the CBO and see whether it is fit for the banks or 
				not. It will help banks to negotiate with the regulator 
				together. The association can be very beneficial for the banking 
				sector.<br>
				<br>
				<b>Best in class</b><br>
				<br>
				&#8207;Dr AbdulRazak Ali Issa, Chief Executive, BankMuscat shares his 
				views on the bank’s achievements in 2011 and the road ahead in 
				an interview with OER<br>
				<br>
				As the leading Omani bank, BankMuscat enjoys a 40 per cent 
				marketshare in Oman, with over $16bn in assets, capital of over 
				$2bn and a customer base of over 1.2mn. The bank is rated ‘A1’ 
				by Moody’s and A- by Standard’s &amp; Poor’s. The bank has an 
				extensive network of branches in Oman, direct and indirect 
				presence in all six GCC states as well as an office in Singapore 
				which focuses on financial institutions and the trade business.
				<br>
				<br>
				&#8207;The opportunities identified by the bank include significant 
				infrastructure development which is expected to contribute to 
				the growth of the banking sector. BankMuscat envisages 
				substantial product cross-sell opportunities and sizeable 
				unbanked market as over 50 per cent of the population is below 
				the age of 19 years. Expansion in the Gulf Cooperation Council (GCC) 
				countries is among the priorities in view of the bank’s direct 
				and indirect presence in all the six states. <br>
				<br>
				&#8207;The bank is well positioned to leverage on the largest network 
				of branches and other delivery channels to target the growth 
				potential and cross-sell opportunities. BankMuscat’s presence 
				has strengthened in all parts of Oman through the widest network 
				of 130 branches, 520 ATM/CDMs and 5,300 PoS terminals. The bank 
				is also set to leverage on investments in new technology and 
				state-of-the-art head office building to further increase 
				efficiency, improve customer service and support growth plan. 
				BankMuscat is well known for adopting innovative strategies to 
				raise capital and enhance stakeholder value. The bank views the 
				industrial development of Oman as a strategic opportunity, 
				especially in Sohar, Salalah and Duqm. &#8207;BankMuscat’s chief 
				executive gives in-depth insights on the bank’s performance and 
				plans for the coming year. Excerpts from an interview. <br>
				&#8207;<br>
				<b>How has BankMuscat’s performance been in financial year 2011?<br>
				</b>&#8207;The key business lines of the bank recorded healthy 
				performance during 2011. BankMuscat’s core business activities 
				are divided into the broad areas of corporate banking, consumer 
				banking, investment banking, treasury, financial institution, 
				private banking and asset management and international 
				operations. Key support functions include information 
				technology, operations, human resources, finance and risk 
				management. In 2011 some of the notable highlights of our 
				performance were as follows:<br>
				&#8207;Ratings upgrade<br>
				&#8207;Standard &amp; Poor’s upgraded BankMuscat’s Long Term Rating to 
				‘A-’ from ‘BBB+’. This was a significant development in the 
				current economic environment signifying the trust reposed in 
				BankMuscat by the analytical and investor community. The Issuer 
				Default Ratings /Long Term Ratings were also affirmed by other 
				rating agencies.<br>
				<br>
				&#8207;<b>Tier II capital <br>
				</b>&#8207;During the year, BankMuscat raised a total of $430mn in 
				Tier II capital of which $170mn was from IFC Capitalisation 
				Fund. The bank also raised $260mn of Tier II capital through a 
				private placement of fixed income subordinated paper with tenors 
				of 61 months to 84 months. The issue attracted considerable 
				interest from institutional investors. &#8207;The Tier II capital 
				raising will bolster BankMuscat’s capital and support the growth 
				in its asset book. BankMuscat is committed to lending its full 
				support to the socio-economic development of Oman and this 
				capital raising will strengthen the bank’s ability to extend 
				finance to further enhance Oman’s development domestically and 
				increase its competitiveness internationally.<br>
				&#8207;<br>
				<b>Al Mazyona</b><br>
				&#8207;As the nation’s leading bank, BankMuscat continued to play the 
				leadership role in promoting the savings culture in the 
				Sultanate. The bank’s flagship al Mazyona savings scheme marked 
				20 years in the Sultanate. On this occasion, the popular al 
				Mazyona scheme was relaunched and customers responded very 
				positively to the relaunch. The bank increased the savings 
				deposits portfolio to achieve a historic milestone of RO1bn. <br>
				<br>
				&#8207;<b>Corporate banking<br>
				</b>&#8207;Strong customer relationship remains fundamental to the 
				growth of our corporate business with a focus on the quality of 
				loan assets and building customer confidence. The bank maintains 
				its emphasis in supporting feasible projects in core areas such 
				as oil and gas, petrochemicals, large-scale industry, shipping 
				and contracting. The bank successfully completed, inter alia, 
				financing of Sohar Free Zone, a specialty steel project, 
				expansion of the cement sector and a syndicated facility for the 
				integrated tourism project Wave. &#8207;The SME department partnered 
				with the Ministry of Commerce &amp; Industry to launch the 
				government guaranteed loan programme in support of SMEs. The 
				$170mn subordinated loan agreement with the IFC Capitalisation 
				Fund was aimed at strengthening the capital base and increasing 
				access to finance for small and medium enterprises (SMEs) and 
				middle-income home buyers. <br>
				&#8207;<br>
				<b>Human resources management</b><br>
				&#8207;BankMuscat achieved a global first, winning the prestigious 
				Level 3 People Capability Maturity Model (PCMM) certification by 
				Carnegie Melon University, USA for adopting benchmark human 
				resources process improvements. BankMuscat has the distinction 
				of outstanding Omani leadership at all levels. The bank’s women 
				empowerment strategy is also notable as 42 per cent of staff are 
				women holding various positions, including senior management 
				positions. <br>
				&#8207;<b><br>
				Information Technology<br>
				</b>&#8207;The Information Technology department runs all its projects 
				and programmes focussed on high business benefits, risk 
				mitigation, compliance or service improvement. Based on 
				recommendations of the Business Continuity Plan (BCP) committee, 
				IT department implemented a state-of-the-art Disaster Recovery 
				Solution providing the bank with a unique capability to ensure 
				uninterrupted service during crisis.<br>
				<br>
				&#8207;<b>International operations:<br>
				</b>&#8207;The year 2011 was a challenging year for the banking sector 
				in the GCC region. Despite the challenging external environment, 
				the performance of the bank’s international entities reflects an 
				improving operating trend. During 2011, the bank marked its 
				presence in Southeast Asia with the opening of its 
				Representative Office in Singapore. With this expansion, the 
				bank is well positioned to benefit from the trade flows between 
				Asia and the GCC, which have witnessed rapid growth in recent 
				years. The focus for the bank now is to strengthen its position 
				in the individual markets, by scaling up businesses and 
				improving operating performances with the aim of deriving the 
				benefits of its international network. <br>
				&#8207;<br>
				<b>Can you tell us about the bank’s road map for FY2012?<br>
				</b>&#8207;The outlook for Oman’s economy in 2012 is positive as the 
				government has announced a 12 per cent increase in spending. In 
				view of the government’s commitment to infrastructure 
				development and industrialisation, BankMuscat anticipates growth 
				opportunities. The good macro-economic growth will create 
				beneficial operating conditions for the bank as the government 
				accelerates financial support, primarily for infrastructure 
				projects. The bank anticipates strong capital levels and a high 
				loss-absorption capacity.&#8207;The opportunity arising from Islamic 
				banking is also expected to help the bank further consolidate 
				operations. In line with the Royal directive on Islamic banking, 
				BankMuscat is set to introduce the most trusted and innovative 
				Islamic banking products. For this purpose, the bank has 
				announced ‘Meethaq’ Islamic banking window operations with a 
				capital of RO150mn, subject to approval of Central Bank of Oman. 
				<br>
