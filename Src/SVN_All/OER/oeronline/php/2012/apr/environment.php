						<font face="Verdana" size="1">
						The concept of sustainable school stems from the ethos 
						of caring for oneself, for each other, across cultures, 
						distances and generations; and for the environment, far 
						and near. Schools are by default caring places, but a 
						sustainable school extends this commitment into new 
						areas. It cares about the energy and the water it 
						consumes, the waste it produces, the food it serves, the 
						traffic it attracts and the difficulties faced by people 
						living in its communities and in other parts of the 
						world. <br>
						<br>
						Driven by such a lofty vision, the Primary School at 
						British School Muscat (BSM) recently hosted a Primary 
						Geography Conference on Sustainability aimed at raising 
						the importance of sustainability within the school and 
						planning for a more sustainable future. The first 
						conference of its kind in the country was attended by 
						delegates from British and International Schools within 
						Oman and speakers and experts on sustainability and 
						conservation. <br>
						<br>
						Ahmed Sayed, Events Manager at United Media Services, 
						did a presentation on Oman Green Awards (OGA), the 
						Sultanate’s first environment awards which has emerged 
						as unique platform to honour and appreciate outstanding 
						environmental vision and achievements in Oman. <br>
						<br>
						“The outcomes from the conference were to look into the 
						possibility of running a sustainability conference for 
						children and a photography competition to raise 
						awareness of the problems of littering in Oman,” says 
						Tamsin Turner, primary humanities coordinator and green 
						team leader at the school. “Most importantly it was an 
						opportunity to recognise the power of working as a team 
						rather than as a series of ‘islands’. I strongly believe 
						that as ‘islands’ we achieve very little but as a team 
						we can make long lasting, valuable and essential changes 
						to our world.”<br>
						<br>
						In BSM, sustainability is integrated in the curriculum 
						of all pupils and students from nursery to year 13. 
						“This is especially strong in science, humanities, 
						literacy and CPSHE but elements are also found in other 
						areas of the curriculum. The Primary School is very 
						lucky to have the expertise of the Senior School to 
						support our work,” adds Turner.<br>
