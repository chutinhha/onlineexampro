						<td valign="top">
						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">
						Home-grown winners<br>
						</font><font face="Verdana" size="2">
                		<br>
						Petroleum Development Oman (PDO) launched its In-Country 
						Value (ICV) proposition to develop local suppliers and 
						manufacturers. Raoul M Restucci, Managing Director 
						speaks elaborately on the subject in this candid 
						interview with Mayank Singh and Visvas Paul D Karra<br>
						&nbsp;</font></b><p align="center" style="margin: 3px 10px">
						<img border="0" src="images/oil&gas-apr-12-1.jpg" hspace="8" vspace="5"><p align="justify" style="margin: 3px 10px">
						<b>
						<font face="Verdana" size="2">
						<br>
                &nbsp;</font></b><p align="justify" style="margin: 3px 10px">
						<font face="Verdana" size="1"><b>Nowadays, all of a 
						sudden people have started talking about In-Country 
						Value (ICV). What does it mean for Petroleum Development 
						Oman (PDO)?</b><br>
						It starts with delivery of our vision which is all about 
						being renowned and respected for the excellence of our 
						people and all the value we bring to Oman and our 
						stakeholders. It goes beyond the delivery of oil and gas 
						and tries to contribute to whatever Oman seeks in value 
						creation.<br>
						<br>
						We are a large business in the oil and gas industry with 
						a high level of activity across a broad spectrum, from 
						well engineering through to construction and development 
						in advanced EOR technologies. And increasingly, we see a 
						lot of opportunities to ensure that these investment are 
						placed locally in terms of services and goods that are 
						made in Oman. We believe that we have the opportunity to 
						further change the amount of local manufacturing 
						capacity to support PDO and the oil and gas industry in 
						Oman and also in many cases to set the standards for 
						delivery of goods and services beyond the boundaries of 
						Oman. Local delivery of goods and services also means 
						immediate support and business continuity for urgent 
						supply requirements, and specific solutions to meet our 
						demands.<br>
						<br>
						<b>You already have Local Community Contractors (LCCs). 
						Are they part of ICV?</b><br>
						Prior to this interview, I had a meeting with an LCC 
						company that has established a gasket manufacturing 
						business in Oman and is very much working and growing 
						with PDO and other operators in the Sultanate. The 
						partnership has led to support in training and in 
						establishing required technical specifications. What is 
						the incubation support needed to address an expansion or 
						delivery of additional goods? There is so much equipment 
						that we need, much of which continues to be sourced 
						externally and the ICV strategy very much targets LCC 
						support to address these manufacturing opportunities, in 
						turn creating considerable direct and indirect economic 
						value through logistics and other services. So it’s all 
						about building on today’s platform and integrating our 
						LCC relationships into a key enabler of the ICV 
						strategy. <br>
						<br>
						<b>Can you elaborate more on the LCCs role in ICV?<br>
						</b>Well, it is literally about making sure that we can 
						support and secure the services and goods of local 
						manufacturers ranging from valves to pumps and tubulars 
						that meet quality and technical specifications, in turn 
						enabling them to secure the broader accreditation such 
						that they can participate in the supply of these oil and 
						gas industry goods at local and regional level.<br>
						<br>
						<b>Going into the definition of ICV, it means that 
						whatever retained earnings we have in the country is 
						being utilised for the priorities that the nation has 
						set for itself. How does that translate into financial 
						numbers?</b><br>
						We have set ourselves some significant stakes in the 
						ground. At the moment, if we take our capital 
						investment, which is in excess of $4bn a year, about a 
						third of that is spent in local service provision and 
						local manufacturing. A relatively small amount pertains 
						to locally manufactured goods, between 10 and 15 per 
						cent, and approximately 45 per cent of all our services 
						are delivered by local companies. Our goal is to 
						dramatically increase this to approximately 50 per cent 
						of our manufacturing requirements and in excess of 75 
						per cent in services, up to 90 per cent in some cases, 
						across the entire oil and gas value chain by 2020. So in 
						financial terms it equates to an annual increment of 
						$100mn in manufacturing and $100mn in services, all 
						targeted for local development. If we take the existing 
						spend plus our annual targets, local spend will exceed 
						$3bn by 2020, adding considerable value to the gross 
						national product, on a direct and indirect basis. <br>
						<br>
						<b>A lot of stress is laid on manufacturing. Is that the 
						way to create employment opportunities?<br>
						</b>The global learning is that employment creation is 
						through a multitude of SMEs with relatively small teams 
						of 5 to 20 employees growing in size over a period of 
						time. SMEs are the economic and employment engines and 
						our goal is to try to incubate and integrate as many 
						entities as possible for long term sustainability. Our 
						logistics challenge is significant. Every day, our staff 
						and contractor community literally drive to the moon and 
						back. So the ability to have local services, local 
						entities that can help us operate across the value chain 
						of oil and gas activities will increase local employment 
						and significantly reduce the driving and logistical 
						challenge we face; a clear win-win on many fronts. <br>
						<br>
						<b>PDO has all along been contributing to the community. 
						Is ICV a continuation of that with a new name?</b><br>
						ICV is just an evolution of PDO’s local content efforts 
						in a more strategic pan-industry approach. So it entails 
						also working with other operators to align the big rules 
						and, for instance, some of the equipment and material 
						technical specifications. If I have one type of pump and 
						if another company has a different type of pump for 
						similar duties then it becomes very difficult and cost 
						inefficient for customers and suppliers alike, but if we 
						can both standardise the equipment then there is an 
						economy of scale which creates efficiencies and reduces 
						local entry barriers. So the ICV effort is a blueprint 
						for acting more strategically, more pan-industry and in 
						a more impactful way than what we have done in the past.<br>
						<br>
						<b>Have you instituted this mechanism like a committee 
						or a formal body to oversee this initiative?</b><br>
						We are privileged to have HE Nasser Khamis Al Jashmi, 
						Undersecretary, Ministry of Oil and Gas, to chair and 
						head the ICV committee which includes senior officials 
						from OPAL, various oil and gas companies in Oman, the 
						Ministry of Finance, the Ministry of Manpower, and other 
						distinguished representatives to progress the ICV 
						agenda. We are discussing what should be the big rules, 
						what are the joint opportunities in training and ideas 
						for the different local propositions. Work is also 
						ongoing to develop a Made in Oman Index that looks at 
						the level of spend, the level of Omanisation, but more 
						importantly the upskilling of the Omani workforce at 
						supervisory and management levels and contribution to 
						domestic value creation.<br>
						<br>
						<b>Will ICV be a cost saving channel for PDO?<br>
						</b>It has to be a sustainable and competitive 
						life-cycle value adding proposition. So in the short 
						term, in some areas the incubational support will cost 
						additional training, purchase of equipment, perhaps 
						funding support for a number of activities; but in the 
						longer term, we firmly believe that it is win-win for 
						local community developers, PDO and other operators. <br>
						<br>
						<b>What kind of hand holding will you be providing to a 
						small SME who want to get into your ICV?<br>
						</b>Let me give you an example of what we are doing for 
						the super LCCs for instance. We have four of them, 
						nominated by a ministerial committee and assigned to PDO 
						at the moment – Al Shawamikh and Al Haditha in the 
						north; and Al Baraka and Al Sahari in the south. We’ve 
						ring fenced work for them from well hoist and slick line 
						work to maintenance and pipeline replacement work. We 
						seconded PDO specialists into each team, and trained or 
						are in the process of training their company staff to 
						deliver on project requirements, we secured the services 
						of financial consultants that work with them to develop 
						robust economic models and business plans and we’ve also 
						ordered and purchased their equipment to the tune of 
						approximately $35mn that allows them to basically 
						kick-off with a running start. <br>
						<br>
						<b>A big drawback for startups is lack of funding. Are 
						you going to put money upfront?<br>
						</b>Yes, for the super LCCs we’ve been able to find an 
						economically viable solution and the question is to 
						understand the requirements for other start-ups. We 
						won’t be able to fund too many things and all will 
						require a clear economic business case.<br>
						<br>
						<b>But what is the arrangement? Will you have a buy back 
						facility?<br>
						</b>Over time, the ownership of the equipment we 
						purchase will likely change but effectively early on, 
						instead of charging the full capital depreciation, they 
						will just charge us an operating fee, as they will be 
						organised to operate that equipment. Therefore the total 
						cost to PDO will not be disadvantaged as the cost of 
						capital for us is obviously much lower and the synergy 
						for purchases will be greater; again it should lead to a 
						win-win situation for both parties.<br>
						<br>
						But you run the risk of being straddled with a lot of 
						start-ups which may or may not deliver…<br>
						So far, our track record with the LCCs in A and B 
						categories, with more than 150 registered companies, 
						some 50 of which are very active, has been totally 
						satisfactory. A vast majority of these companies have 
						been very successful and have grown with us, some to 
						very significant dimensions. With the super LCCs, we are 
						equally putting in a lot of effort and I have no doubt 
						they will succeed and it is in our interest that they do 
						so.<br>
						<br>
						<b>When do you expect the first LCC or company from the 
						incubator programme to begin delivering?</b><br>
						At the moment, I don’t know. If we take the super LCCs, 
						a lot of this equipment is due at the latter part of 
						this year or early next year; so we expect them to 
						become operational immediately thereafter. Presently, 
						they are helping us in arranging and supporting 
						sub-contracting entities. Much effort is going into 
						training. We have monthly discussions with them on key 
						timelines and milestones and so far it’s all progressing 
						at a good pace. <br>
						<br>
						<b>What is the essential qualification for anyone to 
						become an LCC and get into your incubation programme?</b><br>
						I guess the essential qualification is creation of value 
						for Oman. You’ll obviously have to meet technical and 
						quality specifications, HSE requirements and be 
						commercially competitive. But if you have a proposition 
						that creates value for Oman, then we are interested in 
						talking to you.<br>
						<br>
						<b>How much job-creation will happen through ICV?</b><br>
						When His Majesty Sultan Qaboos bin Said called for 
						increased job creation, we set a target of 1,000 jobs 
						per annum for the foreseeable future. And I am 
						privileged to say that together with the contracting 
						community we delivered more than 4,300 jobs. For PDO, 
						that’s just the start. I think you will see considerable 
						efforts to train and develop a significant proportion of 
						the Omani workforce ranging from school leavers to 
						graduates. I believe we can make a difference. Today we 
						have 420 students sponsored by PDO who are training and 
						developing for the contracting community and more than 
						700 new staff developing for key opportunities within 
						and outside PDO, including 189 community scholars. <br>
						<br>
						BOX<br>
						<br>
						<b>ICV has generated a buzz</b><br>
						Building on our local content achievements for the past 
						ten years or so, I am delighted that PDO has embarked in 
						earnest on this exciting ICV journey. Nowadays, 
						throughout the organisation, not only in contracting and 
						procurement one can feel the buzz that ICV has 
						generated. It would be naive for us to assume that it’s 
						all worked out, however directionally we are clear and 
						in the last six months we have achieved a lot including 
						alignment across the oil and gas industry with regards 
						to how we collectively want to deliver ICV. <br>
						<br>
						We now have the full backing of the PDO board, our ICV 
						policy has been published and we have a dedicated team 
						to drive the ICV agenda. Our key challenges continue to 
						be mindset change both internally and externally, where 
						we all need to believe wholeheartedly that ICV makes 
						business sense both for PDO and its suppliers. 
						Opportunities exist across PDO’s entire portfolio, be it 
						in well engineering, project delivery etc. Having said 
						that, there is ample evidence that PDO and its suppliers 
						are rising to the challenge and I am confident that 
						collectively we will deliver.<br>
