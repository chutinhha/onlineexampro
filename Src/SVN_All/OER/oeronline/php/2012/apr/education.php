						<p align="justify" style="margin: 3px 10px">
				<b>
				<font face="Verdana" size="4" color="#C00000">Creating a legacy 
				of excellence</font></b><p align="justify" class="style13">
				National Training Institute has invited nominations for the 
				sixth edition of NTI BizPro Awards<br>
				<br>
				<br>
				<img border="0" src="images/education-apr-12-1.jpg" width="200" height="181" align="right" hspace="12" vspace="3">The sixth edition of National Training Institute (NTI) BizPro 
				Awards 2012 was announced earlier this year with an aim to 
				acknowledge and reward Omanis who have shown creativity and 
				revolution in pursuit of excellence in leadership skills. The 
				awards promote the important achievements of Omani business 
				professionals in the private sector. However, the message for 
				the Awards this year has a wider focus that includes the society 
				as well.<br>
				<br>
				The guest speaker in 2012 is the social awareness campaigner, 
				Yuthar Al Rawahy, founder and chairperson of the National 
				Association for Cancer Awareness (NACA). Yuthar is a three-time 
				cancer survivor, who has dedicated her life to promoting 
				awareness, advocacy and research to enable early diagnosis and 
				treatment of those suffering from cancer in Oman.<br>
				<br>
				“Sharing one’s good fortune with society – as business leaders 
				and as organisations – contributes positively to building 
				strong, credible brands, as well as a sustainable and loyal 
				customer-base,” says Lawrence Alva, CEO, NTI. The NTI BizPro 
				Awards will have two categories; Business Leader Award and Young 
				Achievers Award. He continued, “The main objective of these 
				awards has been to reward and promote the achievements of the 
				Omani Business Leaders and Young Achievers by acknowledging 
				their contribution to the corporate sector in the Sultanate”<br>
				<br>
				<img border="0" src="images/education-apr-12-2.jpg" width="200" height="342" align="right" hspace="13" vspace="3"><b>Promoting achievements <br>
				</b>The main objective of these awards has been to reward and 
				promote the achievements of the Omani Business Leaders and Young 
				Achievers.<br>
				<br>
				To be eligible for the nomination for the Business Leader Award 
				the person should be an Omani national, employed at a senior 
				management role and must have made substantial contribution to 
				the organisation and society. “The corporate sector and the 
				media play a very important role in the growth and development 
				of a country, therefore nominations are invited exclusively from 
				the Top 50 businesses and the media fraternity for nominating 
				three leading business personalities from Oman,” said Poonam 
				Sonakia, operations manager, NTI. To be eligible for the Young 
				Achievers Award the nominee should be in the age group of 25 to 
				35 years, and have a minimum 5 years of work experience. The 
				nominee should also have management aptitude, leadership 
				qualities and effective communication skills and should be able 
				to train, support and encourage team members. KPMG in Oman, a 
				leading provider of audit, tax and advisory services evaluates 
				the candidates. All the nominations are received and reviewed 
				directly by KPMG.<br>
				<br>
				<b>Source of inspiration<br>
				</b>BizPro Young Achievers Award is an endeavour to be a true 
				source of inspiration, motivation and encouragement for Omani 
				youth.<br>
				<br>
				Under Young Achievers Award the award committee filters down 
				business professionals from hundreds of nominees. The selected 
				participants will then participate in screening management 
				aptitude test out of which 12 finalists will be selected. Based 
				on the assessment done by the award committee three finalists 
				will be chosen as winners. Under the Business Leader Award the 
				finalist will be selected based on the nomination count per 
				nominee. KPMG will invite additional information from all 
				finalists and a panel will select the winners based on 
				nomination information and additional information received from 
				finalists. <br>
				<br>
				<b>Scholarship <br>
				</b>The winner of the Business Leader Award will receive a 
				scholarship of Rial RO5000 for developing leaders in their 
				organisation while each of the winners of the Young Achievers 
				Award will receive a scholarship of RO2500 to enhance their 
				management education. Both the scholarships will be sponsored by 
				Renaissance Services.<br>
				<br>
				Details on the eligibility, nomination and evaluation process 
				for NTI BizPro Awards 2012 for Young Achievers and Business 
				Leaders are available at the website,
				<a href="http://www.ntioman.com/bizpro.shtml">
				www.ntioman.com/bizpro.shtml</a>.&nbsp; <br>
