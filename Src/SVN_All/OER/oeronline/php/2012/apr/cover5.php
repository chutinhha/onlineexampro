						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">
						A robust showing</font></b><p align="justify" class="style13" dir="ltr">
				<br>
				<b>Growing on par</b><br>
				<br>
				Broadening its customer base by strengthening its retail 
				operations has been the way forward for Bank of Baroda. This has 
				helped the bank to continue to add the required numbers to its 
				books<br>
				<br>
				<br>
				Bank of Baroda (BoB), the Indian based bank, has witnessed 
				appreciable growth in its customer base thanks to its sustained 
				efforts to provide value-added services. “We have been able to 
				broaden our retail base which has led to a substantial growth in 
				opening new saving bank accounts even while the personal loans 
				segment continued to grow. We added around 2,200 new customers 
				to the bank in 2011 as compared to around 1,300 in the previous 
				year,” says Devinder Pal, country head, Oman operations.<br>
				<br>
				Connecting to the Oman Switch ATM network has been one of the 
				highlights of a number of customer-oriented initiatives 
				undertaken by BoB in 2011 and is probably one of the reasons for 
				the increase in new savings accounts. The Oman Switch allows the 
				bank’s customers to withdraw cash from ATMs of any bank in the 
				country except OIB. Pal informs that the bank does not charge 
				its customers any service fee for using the cash dispensing 
				machines of other banks. One more aspect of the bank is its 
				ability as a full-fledged bank to offer all types of services 
				including personal loans, corporate and retail lending 
				facilities, project finance for SMEs. “Such things have gone a 
				long way in not only helping us to retain our existing clients 
				but in adding the numbers to our customer base,” Pal adds.<br>
				<br>
				Looking at the analysis of the year-on-year growth, the loan and 
				advances of BoB grew by 12.7 per cent while the deposits went up 
				by 5.4 per cent. BoB’s total capital adequacy ratio in Oman is 
				27 per cent as of December 2011 which means that the bank is 
				well capitalised. Meanwhile, the bank’s allocated capital was 
				increased by 18.7 per cent by way of induction of funds by the 
				head office as well as appropriation of retail earnings in order 
				to comply with the Central Bank of Oman (CBO)’s regulatory 
				norms. <br>
				<br>
				The net interest income increased by 16.7 per cent while net 
				profit show an increase of 4.4 per cent. However, as compared to 
				2010, BoB’s growth in 2011 was marginally reduced but the bank 
				was able to maintain itself at industry levels. “The banking 
				industry grew at 16 per cent in advances and we grew at 12 per 
				cent,” points out Pal. Talking about growth of the bank in 2012, 
				Pal says, “this year, we are concentrating on the opening of our 
				Sohar branch as we have got CBO approval and we will look 
				forward to strengthening the operations there. Likewise, the 
				emphasis will be on increasing our customer base. Many of our 
				customers are into trading and contracting activities and our 
				continued support to them will help us in the future. Of course, 
				we are financing projects in other major sectors as well 
				whenever needed.”<br>
				BoB began its operations in Oman in 1976 and has become a modern 
				bank with state-of-the-art technology serving every segment of 
				the society in the Sultanate. Customers’ convenience and 
				competition are the key factors driving the bank’s technology.<br>
				<br>
				In the GCC region, BoB is present in UAE through its six 
				branches and eight electronic banking service units and also has 
				a wholesale banking branch in Bahrain. The bank is one of the 
				leading public sector banks in India, committed to the service 
				of individual and corporate customers in India and abroad. BoB 
				has 87 global offices in 26 countries including that of its 
				subsidiaries. In India, the bank boasts of 3,757 total domestic 
				branches and made a net profit of $656.96mn for the nine month 
				period ending December 2011. The total global deposits of the 
				bank was $65,757mn with advances totaling $49,084mn during the 
				same period. <br>
				<br>
				<br>
				<b>Upbeat prospects<br>
				</b><br>
				Standard Chartered Bank will continue to strengthen and manage 
				its balance sheet in a disciplined manner, focusing on existing 
				clients and expanding its products portfolio<br>
				<br>
				<br>
				Standard Chartered has made steady progress on the consumer 
				banking transformation journey during 2011 with this segment 
				seeing significant growth in loan products, SME and wealth 
				management products and services. The staff number has increased 
				by nine per cent overall which demonstrates its growth as a bank 
				and ability to deliver on its promise to its customer base.<br>
				<br>
				“With a ninth consecutive year of record income and profit, our 
				balance sheet remains in excellent shape; highly liquid, 
				strongly capitalised and well diversified, and the business 
				momentum has continued both in consumer banking and wholesale 
				banking,” says Ravneet Chowdhury, CEO, Standard Chartered Bank. 
				Continuing further, Chowdhury says that business is doing well 
				as Oman has a good GDP rate. The bank’s consumer banking 
				franchise is expanding rapidly while at the same time, the 
				product side has also been beefed up with new products. This has 
				helped the bank to see growth in new business. The bank has 
				expanded its market by including varieties of cash management 
				products.<br>
				<br>
				<b>Resilient momentum<br>
				</b>Despite the continued turmoil in the financial markets, 
				Standard Chartered’s strategy remains the same: Aspire to be the 
				world’s best international bank, leading the way in Asia, Africa 
				and the Middle East and build deep, longstanding relationships 
				with its customers and clients. The diversity of different 
				businesses and geographic markets also give the bank resilient 
				income momentum and enables it to grow even when some markets 
				have a difficult year. <br>
				Says Chowdhury, “In each of the last five years, we have 
				increased capital levels, staff numbers, earnings per share and 
				dividends, as well as income and profits. We have a policy of 
				maintaining a strong capital and liquidity position, which 
				enables us to stay open for business and take marketshare 
				through the economic cycle in core areas of business. It should 
				be no surprise that we are the only international bank to have 
				been upgraded by all three major ratings agencies since the 
				financial crisis.”<br>
				<br>
				<b>Expansion plans<br>
				</b>According to Chowdhury, the bank will launch its priority 
				banking services as part of its growth plans in 2012. A third 
				branch is also in the offing and it will be located in Qurum.
				<br>
				<br>
				The bank continues to develop its local talents through the Al 
				Massar programme which is designed to provide Omani nationals 
				with a variety of tools that will improve their performance and 
				engagement. “Developing the future leaders is a high priority, 
				hence we will continue to train and invest in the Omani 
				nationals inside and outside the bank. We will maintain the 
				highest level of operational discipline and continue to comply 
				and cooperate with the Central Bank of Oman,” Chowdhury adds. 
				Apart from all this, the bank also aims to grow its business 
				with the government sector through SME and wholesale business.<br>
