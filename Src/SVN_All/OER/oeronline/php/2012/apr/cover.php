						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">
						A robust showing </font></b><p class="style15">
				<span class="style14"><strong>The OER-Gulf Baader Capital 
				Markets survey of the Best Banks in Oman 2011 sees prudent and 
				steady players doing well. The following pages present a 
				comprehensive overview of the Sultanate’s banking sector </strong></span><p align="center" class="style13">
				<font face="Verdana" size="1">
				<img border="0" src="images/cover-apr-12-1.jpg" style="border-style: solid; border-color: #808080;" class="style17" width="500" height="747"></font><p align="justify" class="style13" dir="ltr">
				The global finance industry may not be going through the best 
				of times, but Oman’s banking sector has put up a performance 
				that can be summed up in one word – consistency. This finds a 
				reflection in the OER-Gulf Baader Capital Markets Best Bank’s in 
				Oman survey 2011. <br>
				<br>
				&#8207;<b>Industry structure and performance<br>
				</b>&#8207;The domestic economic scenario witnessed improvement in 
				2011, driven by higher oil&#8207; revenue and prudent macro-economic 
				policies adopted by the government. Higher than &#8207;forecasted oil 
				revenue is expected to result in budget surplus for the year 
				despite an &#8207;increase in government spending. Inflation has 
				remained at acceptable levels and IMF &#8207;forecasts inflation to 
				moderate to 3.3 per cent in 2012. <br>
				<br>
				&#8207;It is also noteworthy that the domestic economy and the banking 
				system were not adversely impacted by various regional and 
				international events. The Central Bank of Oman (CBO) has 
				expressed its satisfaction with the current position in its 
				review of banking and monetary developments in 2011. Despite 
				adverse international developments, the Omani banking sector 
				continued to show optimism and resilience during 2011, 
				consistent with the good performance of the real economy.<br>
				<br>
				&#8207;The banking industry in the Sultanate is mature, developed and 
				highly competitive. There are seven local and ten foreign banks 
				in the market creating both opportunities and challenges to 
				achieve optimal performance. The economic and banking industry 
				outlook for the year 2012 is positive as a result of prudent 
				macro-economic initiatives implemented by the government. The 
				fiscal budget for 2012 has a strong emphasis on growth. 
				Privatisation and infrastructure development are among major 
				focuses to achieve economic &#8207;growth and diversification.<br>
				<br>
				&#8207;The CBO continued to pursue an accommodative monetary policy 
				during 2011 to support economic recovery. Consistent with 
				sustained growth of the real economy, total assets of commercial 
				banks jumped 17.5 per cent to RO18.4bn as of December 2011, from 
				RO15.64bn for the corresponding period of 2010. Aggregate 
				deposits of commercial banks, on a year-on-year basis, increased 
				to RO12.6bn during 2011, from RO10.51bn in 2010. The credit 
				growth also accelerated by 16.6 per cent to RO12.51bn as of 
				December 2011 from RO10.72bn during the same period in the 
				previous year.<br>
				<br>
				&#8207;Reflecting comfortable liquidity condition, both deposit and 
				lending rates in Oman moved &#8207;downward over the year. While the 
				weighted average rial deposit rate fell from 1.85 per cent in 
				September 2010 to 1.45 per cent in September 2011, the weighted 
				average rial lending rate declined from 6.98 per cent to 6.43 
				per cent during the same period. &#8207;Commercial banks’ interest 
				rate spread was under mild pressure as it fell from 5.14 per 
				cent in September 2010 to 4.98 per cent in September 2011.<br>
				<br>
				&#8207;&#8207;Signalling international confidence in the Omani banking 
				sector, global credit rating agency &#8207;Standard &amp; Poor’s upgraded 
				the Banking Industry Country Risk Assessment (BICRA) rating to 
				group ‘4’ from group ‘5’ in November 2011. The rating agency 
				also upgraded Oman’s economic risk score to ‘4’ from ‘6’ and 
				assigned an industry score of ‘4.’ S&amp;P’s BICRA analysis for a 
				country is designed to evaluate and compare global banking 
				systems on a scale from ‘1’ to ‘10,’ ranging from the 
				lowest-risk banking system (Group 1) to the &#8207;highest-risk 
				banking systems (Group 10). After the upgrade, Oman stands with 
				Brazil, Kuwait, Malaysia, South Africa and Taiwan in Group 4. In 
				a statement on its website, S&amp;P said, “Oman’s institutional 
				framework is shaped by what we view as the regulator’s good 
				track record, and adequate regulation and supervision, which are 
				increasingly aligned with international standards.”<br>
				<br>
				&#8207;In a major development with important ramifications for the 
				banking and financial sector, the Omani government gave its nod 
				for the introduction of Islamic banking services in the 
				Sultanate. Following Royal Order in this regard by His Majesty 
				Sultan Qaboos bin Said in May 2011, the CBO issued a landmark 
				circular (BM 1081) to all licensed banks operating in the 
				Sultanate. The circular authorises the conduct of Islamic 
				banking through exclusive Islamic banks and windows of existing 
				licensed banks. The CBO has also mandated a minimum capital 
				requirement of RO10mn for Islamic banking windows.<br>
				<br>
				&#8207;A number of leading Omani commercial banks have since unveiled 
				plans and strategies to offer shariah-compliant banking products 
				and services to their customers. The market regulator, the CBO 
				has also issued licenses for the establishment of two new banks 
				– Bank Nizwa and Al Izz International Bank – which will 
				exclusively focus on offering Islamic banking services. The new 
				banks are expected to commence operations as soon as the legal 
				and regulatory framework for Islamic banks is finalised. Bank 
				Nizwa and Al Izz International Bank are expected to start 
				operations sometime in 2012.<br>
				<br>
				&#8207;<b>And the winner is …<br>
				</b>&#8207;As expected, the Best Banks 2011 survey throws up quite a 
				few surprises. Ahli Bank has emerged as the No. 1 bank in Oman 
				based on an all round performance by reporting stronger growth 
				along with better asset quality and productivity. The bank has 
				been able to show stronger performance during the last three 
				years. Bank Dhofar has been ranked No.2 with the bank continuing 
				its stable performance despite the presence of one off expenses 
				during 2011. In terms of efficiency parameter, the bank holds 
				No. 2 position. Oman Arab Bank has come out as No.3 bank in the 
				ranking with the bank doing well on sustainability. The bank has 
				maintained its No.1 position in terms of efficiency, which is 
				noteworthy.<br>
				<br>
				Says Kanaga Sundar, senior manager- research, Gulf Baader 
				Capital Markets, “Bank Muscat stood at No. 4 in the overall 
				ranking due to the prevailing legacy issues and the sustained 
				level of growth has to come in from a higher base. Despite this, 
				the bank has maintained its No. 1 position in terms of 
				productivity.” Bank Sohar, the new entrant to the current year 
				ranking process has come out as No. 5 with a strong performance 
				in growth cum asset quality parameters.<br>
				<br>
				&#8207;National Bank of Oman stood as No. 6 in the overall ranking, 
				though it fared much better in terms of growth. Oman 
				International Bank has been ranked as No. 7 in the ranking with 
				the bank reporting lower growth rates as compared to the 
				industry during the last three years.<br>
				<br>
				&#8207;<b>Crystal gazing <br>
				</b>&#8207;Oman’s 2012 budget foresees a nine per cent growth in 
				public spending, with an allocation of a total of RO10bn for 
				public expenditure. There is a clear focus on social aspects 
				&#8207;with increased current expenditure on areas such as education, 
				health and social &#8207;security by 18 per cent. The expected 
				spending level in 2012 will be slightly more than &#8207;2011 outlays. 
				The nine per cent growth in public spending covers most of this 
				committed &#8207;expenditure, as well as additional requirements 
				resulting from providing increased &#8207;employment opportunities for 
				the Sultanate’s citizens. And these augur well for the growth of 
				the Sultanate’s banking sector. <br>
				<br>
				<b>Maintaining momentum <br>
				</b><br>
				HE Hamood Sangour Al-Zadjali, Executive President, Central Bank 
				of Oman shares his views on the Sultanate’s banking sector in an 
				exclusive interview with OER<br>
				<br>
				<br>
				<b>How was the banking sector’s performance in 2011?<br>
				</b>Consistent with the sustained recovery of the real economy, 
				Omani banking sector continued to exhibit accelerated growth and 
				optimism during 2011. Total assets of commercial banks increased 
				by 17.5 per cent to RO18.4bn in 2011 compared to RO15.6bn in the 
				previous year. Aggregate deposits of commercial banks, on a 
				year-on-year basis, increased by 19.6 per cent to RO12.6bn as at 
				the end of December 2011. Total credit expanded by 16.7 per cent 
				during the year to reach RO12.5bn. Commercial banks earned a 
				provisional net profit of RO265.7mn in 2011 which was 7.3 
				percent higher than RO247.7mn registered in the previous year. 
				The system-wise capital adequacy ratio for commercial banks as a 
				percentage of total risk-weighted assets remained at a 
				satisfactory level of around 16 per cent as against the minimum 
				mandatory requirement of 12 per cent prescribed by the CBO.<br>
				<br>
				<b>What were the regulatory measures taken by the Central Bank 
				of Oman (CBO) to strengthen the banking sector in the Sultanate?</b><br>
				Although financial stability was maintained in Oman even during 
				the worst phase of the recent global financial crisis, CBO’s 
				endeavour has been to safeguard and strengthen the financial 
				system in the medium term against global and regional shocks. 
				The CBO initiated a number of regulatory and supervisory 
				measures to improve efficiency of the banking sector. The Omani 
				banking system adopted Basel II capital adequacy framework since 
				January 2007. Risk-based supervision is being implemented in 
				Oman and the information is being integrated with the off-site 
				monitoring to keep a constant vigil over the banking system. The 
				system level aggregation of micro-prudential indicators is being 
				done to generate financial soundness indicators. Since financial 
				stability has emerged as a global problem, CBO has taken timely 
				action in setting up a financial stability unit for 
				macro-prudential supervision of the financial system and to 
				produce financial stability reports on a regular basis. <br>
				<br>
				<b>The global economy has been going through a phase of 
				uncertainty, has this had an impact on Oman’s macro-economy?</b><br>
				Despite global uncertainties, the Omani economy continues to 
				maintain the overall growth momentum mainly due to sustained 
				rise in crude oil prices in the international markets. Increase 
				in government expenditure and easy monetary policy also 
				contributed to maintain domestic demand. The slowdown in global 
				merchandise trade had limited impact on Omani exports as the 
				direction of Omani exports has been more diversified towards the 
				emerging market economies. However, the downside risks to growth 
				in Oman will be due to prevailing global uncertainties, 
				particularly in the euro area. Both the government and CBO are 
				keeping a close vigil over the global developments and shall 
				take necessary action as and when required to contain the 
				spillover of the global slowdown.<br>
				<br>
				<b>The CBO has allowed Islamic banking in Oman, what impact will 
				this have on the banking sector and the economy at large? How 
				much ground work has been done regarding Islamic banking and 
				when can we expect the first set of regulations to come out?</b><br>
				The CBO is confident that the advent of Islamic banking will 
				have a positive impact on the economy. Oman offers a large 
				potential for the growth of Islamic banking, opening up new 
				segments and players both from banks within and investors from 
				abroad and thus providing opportunities for new foreign 
				investments. Banks are optimistic that they will be in a 
				position to significantly increase business and this will augur 
				well for the economy as a whole. Islamic banking is expected to 
				complement the conventional banking in promoting development in 
				the economy. In a competitive environment, bank customers should 
				get the benefit of choosing between both conventional and 
				Islamic banking products. This will enhance product 
				diversification in the banking system. <br>
				<br>
				The CBO has already given licenses for two new Islamic banks and 
				allowed existing conventional banks to open windows for Islamic 
				products with appropriate segregation of funds. They will become 
				operational as soon as formalities such as regulatory 
				requirements, raising of fresh capital and constitution of 
				management are met. The CBO is finalising the framework of 
				regulations in consultation with banks and it is expected to be 
				out by the first half of the year. <br>
				<br>
				<b>How do you see the banking sector in Oman performing in 2012?</b><br>
				The banking sector in Oman continued its optimistic growth trend 
				in 2011 consistent with the overall GDP growth. Banks remained 
				sound, resilient and profitable due to appropriate regulatory 
				and supervisory policies adopted by the CBO. The Eighth 
				Five-Year Development Plan (2011-2015) aims at promoting an 
				economic environment capable of sustaining long-term growth. The 
				pace of economic diversification will further promote the role 
				of the private sector in the development process. Under the 
				plan, the CBO will continue to enhance the role of the banking 
				sector in the economic development by encouraging credit growth 
				to productive sectors including SMEs. The advent of Islamic 
				banking is expected to complement the current conventional 
				banking in promoting growth in the economy. The outlook for 2012 
				remains positive with increased business for banks amidst a 
				stable domestic macroeconomic environment.<br>
