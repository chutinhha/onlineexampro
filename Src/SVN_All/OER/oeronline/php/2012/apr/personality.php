						<td valign="top">
						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">
						A meticulous approach<br>
						</font><font face="Verdana" size="2">
                		<br>
						Sayyid Khalid Bin Hamed Bin Saif Al Busaidy, Chairman, 
						Al-Nab’a – Holding shares his inspirations, management 
						style and reasons for his success with Mayank Singh in 
						an exclusive interview<br>
						&nbsp;</font></b><p align="center" style="margin: 3px 10px">
						<img border="0" src="images/personality-apr-12-1.jpg" hspace="8" vspace="5" width="500" height="333"><p align="justify" style="margin: 3px 10px">
						<b>
						<font face="Verdana" size="2">
						<br>
                &nbsp;</font></b><p align="justify" style="margin: 3px 10px">
						<font face="Verdana" size="1"><b>Can you give us a brief 
						background of your educational and professional 
						background? <br>
						</b>The company started in 1984, while I was in the US 
						doing my graduation in Economics from Capital University 
						in Columbus, Ohio. I was on a government scholarship and 
						joined the University project when I came back, then 
						they immediately sent me to do a masters from Marymount 
						University in Arlington Virginia in 1985-86. I got my 
						MBA in 1986 and came back. I was one of the people who 
						started the University (SQU) Hospital. From 1987-1994, I 
						was the director of Administration and Finance at the 
						University Hospital and then I was shifted as the 
						director of Coordination and Follow Up in the Vice 
						Chancellor’s office, where I was posted between 
						1994-1998. I became the pro vice chancellor of 
						Administration and Finance for SQU, including the 
						hospital from 1998-2002. I continued as the advisor to 
						the Vice Chancellor till 2008. <br>
						<br>
						In 1986 we got a franchise from a US company called 
						Service Master. Al-Nab’a was established as Al-Nab’a 
						Services and converted into Al-Nab’a Service Masters. 
						This was between 1986 till 1997. US companies take a 
						royalty from the turnover rather than the net profit and 
						this was proving to be a burden. The economic conditions 
						in the early part of the 1990’s was not that good, and 
						so we came to an understanding with Service Master that 
						we will terminate the contract. Originally the contract 
						was for 23 years but from 1997 we started working 
						independently.<br>
						<br>
						At that time, the company had a housekeeping division, 
						pest control, waste management and general maintenance 
						service. The real growth of Al-Nab’a came after 2001 and 
						since then there have been times when we have grown by 
						30-40 per cent a year. A big factor for this was the 
						fact that I started to concentrate on the business full 
						time. In 2007 we started Al Nab’a catering, and while 
						this started to serve our own camps, soon we started 
						taking contracts from outside. Al Nab’a Catering has 
						grown phenomenally and today the company has a turnover 
						of nearly RO4mn with a staff strength of almost 500. <br>
						<br>
						In 2008 we converted the general maintenance division 
						into a construction company and till now we are doing 
						our own projects, like labour camps, buildings etc. Al-Nab’a 
						construction has a strength of almost 400 people. The 
						group as a whole has over 5600 people, in 2000 we had 
						just 1,000 people. We have recently converted the 
						company into Al-Nab’a infrastructure, as we wanted to 
						expand and go into other areas other than civil 
						contracts. In infrastructure we have a joint venture 
						with C&amp;C of Delhi, BSCPL from Hyderabad and as a 
						consortium we are working on getting a road construction 
						or infrastructure project. At Al Naba Equipment &amp; 
						Supplies, we are distributors of a number of other 
						brands like ‘Beldos-Portion Control Equipment’ from 
						Belgium, ‘Kromluks-Kitchen/Laundry/Hospital cooling 
						equipments from Turkey, Automated Chapatti making 
						machines from Neoconept – Delhi and Coimbatore Kitchens 
						from Tamilnadu, India.<br>
						<br>
						<b>You have a strong background in HR and finance, has 
						that helped you in your business?</b><br>
						Both my management and finance background have helped 
						me. Internal audit and finance are the backbone of any 
						organisation. If you have strong internal audit and 
						planning you can have a really good business. A long 
						term stable and steady business requires two main things 
						–one, a strong management system in terms of policies 
						and procedures plus reporting systems for every unit of 
						the business. Secondly, you need highly professional 
						people to run that system. Neither one can be successful 
						without the other. That’s the reason why I have insisted 
						on ISO certifications; HACCP in the catering business 
						etc.<br>
						<br>
						We got an ISO certification in 2008 and became the first 
						company to have such a certification in the facility 
						management business. To ensure the long term 
						sustainability of the business we established Al-Nab’a 
						Holding in 2008. The Holding company was established in 
						2008 to look after the whole group, management control, 
						diversification, new business opportunities etc. I have 
						opted for a Board of Trustees to oversee the company, 
						compared to a Board of Directors. A management 
						consultant Finteq Enterprises, Kolkata, India is 
						assisting us in improving this system.<br>
						<br>
						<b>Why have you opted for a Trustee based succession 
						system in place of a Board of Directors?<br>
						</b>My children are still at the University level and if 
						God forbid, anything untoward happens then this Board of 
						Trustees will take care of the business till they are 
						ready to take over. Our legal advisor also sits on the 
						Board of Trustees. The executive management, assistant 
						general managers, the legal advisor and the management 
						consultant meet every quarter and review strategic 
						issues.<br>
						<br>
						<b>Facility management is very labour intensive business 
						with a high HR turnover? How do you manage the business?</b><br>
						One of the main challenges that we face is the turnover 
						of manpower. You recruit people, train them and put them 
						on the job, if someone leaves suddenly, then it requires 
						us to go through the process all over again. In this 
						kind of a business the turnover is relatively on the 
						higher side. To counter this we have a very strong 
						recruitment team, who work on getting replacements soon 
						and there is a system in the pipeline. The other way of 
						retaining people is by giving them better facilities and 
						fringe benefits like good wages, overtime, 
						accommodation, food and medical facilities.<br>
						<br>
						<b>How would you describe your management style – is it 
						participatory or centralised?<br>
						</b>I believe in two or three things – putting in place 
						a good system, identifying highly professional people, 
						and then giving them the authority and responsibility, 
						so I believe in decentralisation of authority. Secondly, 
						I like being transparent, we discuss ideas in a very 
						open way and then come to a decision.<br>
						<br>
						<b>You have built up Al-Nab’a from scratch. What 
						accounts for your success and is there anyone from whom 
						you have learnt a few things in life?</b><br>
						On the personal front, I have learnt a lot from my 
						father, Hamad bin Saif al Busaidi who was a great 
						scholar and he taught me a lot of good principles in 
						life like having respect for people and the importance 
						of relationships. Second, is commitment, once you commit 
						yourself then you need to do things and that’s the 
						reason why I am committed to the business to satisfy 
						God, myself and the people around. From a management 
						point of view I learnt a lot from my late father in law 
						– Sheikh Saud Bahwan, the founder of the Saud Bahwan 
						Group and one of the most successful businessmen in Oman 
						and HE Mohammed Zubair, as he was my boss at the 
						University as the Vice Chancellor. I learnt the 
						importance of commitment, achieving your targets, 
						hardwork and the importance of time from these two 
						business leaders.<br>
						<br>
						<b>What is your advice to youngsters in Oman?</b><br>
						First they have to be honest with themselves in whatever 
						they want to do, whether it is education or work and 
						they need to be committed. Once they have committed to 
						do a thing then they have to do their level best to 
						achieve their goals. This will help them and the society 
						as a whole. To achieve this would require extreme 
						hardwork. As Indira Gandhi, the late prime minister of 
						India said, “The harder I work the luckier I get.”<br>
						<br>
						<b>What are your future plans for Al-Nab’a Holding?</b><br>
						We have set our sights on diversification and this is 
						happening in two ways. We have established a branch in 
						the UAE, so we are diversifying geographically, whether 
						it is for facility management, trade or catering 
						services. After establishing ourselves in the UAE we 
						will go to Qatar in a big way. We have a small office in 
						the UAE and Saudi Arabia, but we want to strengthen our 
						presence in the UAE. We are also looking at getting into 
						an industrial joint venture with good manufacturing 
						companies as this may be good for us in the long run. </font></td>
