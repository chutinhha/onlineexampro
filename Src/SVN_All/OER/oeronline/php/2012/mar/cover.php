						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">
						MICE is right for Oman</font></b><p class="style15">
				<span class="style14"><strong>The Sultanate is well placed to 
				grab a share of the multi-billion dollar business tourism market 
				thanks to the strong foundation it is laying currently. Visvas 
				Paul D Karra reports on how Oman is emerging as the destination 
				of choice for the global corporate traveller</strong></span><p align="center" class="style13">
				<font face="Verdana" size="1">
				<img border="0" src="images/cover-mar-12-1.jpg" style="border-style: solid; border-color: #808080;" class="style17" width="500" height="560"></font><p align="justify" class="style13" dir="ltr">
				<b>In Event 1: Cardiology conference</b><br>
				Date: Jan 2012<br>
				Participants: 800<br>
				Place: Muscat<br>
				<br>
				<b>Event 2: Religious Cadres Forum</b><br>
				Date: Feb, 2012<br>
				Participants: 2,600<br>
				Place: Muscat<br>
				<br>
				<br>
				The above jottings seem to have been lifted straight out of an 
				executive diary. Actually, these are a few, out of a growing 
				list of events that have recently taken place in Oman. The scale 
				and participation at these events is striking -- the Cardiology 
				conference and Religious Cadres Forum attracted 3,400 delegates, 
				who stayed at five star hotels in Muscat and went sight-seeing 
				and shopping on the sidelines of the event. Welcome to Meetings, 
				Incentives, Conferences and Exhibitions (MICE) tourism in Oman.<br>
				<br>
				Globally and within the GCC region, the MICE market is very 
				strong and central to many national tourism strategies. Feedback 
				from Muscat’s leading resorts and hotels indicate that MICE and 
				associated activity contributes to around 35 per cent of their 
				total revenue, and this is increasing by the day. Although 
				Oman’s ability to attract large conferences is limited to the 
				current seating capacity of around 1,000, there is no dearth of 
				conferences and exhibitions in Oman all year round.<br>
				<br>
				<b>The groundwork<br>
				</b>There are more than 200 convention centres around 150 
				countries that are actively vying for their piece of the 
				international conventions and meetings industry pie. Figures 
				from the International Convention &amp; Congress Association (ICCA) 
				state that the number of international association meetings held 
				throughout the world are around 9,210 on a regular basis (see 
				Table 2 for break up). Out of this only 129 or 1.41 per cent are 
				held in the Middle East/Africa region. This shows the growth 
				potential of MICE in the MENA region. The top five cities for 
				International Association Meetings as per ICCA 2010 Annual 
				Statistical report are Vienna, Barcelona, Paris, Berlin and 
				Singapore.<br>
				<br>
				MICE tourism usually includes a well-planned agenda centered 
				around a particular theme or topic, such as profession, trade, 
				organisation, a special interest group or an educational topic. 
				Such tourism is often a specialised area with its own 
				exhibition. Location of MICE events are normally bid by 
				specialised convention bureaux located in particular countries 
				and cities and established for the purpose of bidding on MICE 
				activities. This process of marketing and bidding is normally 
				conducted well in advance of the actual event, often several 
				years ahead and therefore is known for its extensive planning 
				and demanding clientele.<br>
				<br>
				<b>Important segment<br>
				</b>So, is Oman really emerging as the regional and global MICE 
				destination? Industry experts are optimistic because the country 
				already has quality resorts, hotels and a conferencing platform 
				to build upon, and its tourism offers – an authentic Arabian 
				experience in a unique setting which appeals to international 
				conference organisers and major corporations looking for a niche 
				conference location.<br>
				<br>
				MICE is an important segment for the tourism strategy of Oman, 
				admits HE Maitha Saif al Mahrouqi, undersecretary, Ministry of 
				Tourism, because 10 per cent of MICE travellers return as 
				regular tourists. Keeping this in mind, the ministry is also 
				working closely with organisations like the Oman Chamber of 
				Commerce and Industry (OCCI) which are inviting business 
				delegations on a regular basis, says HE Maitha while adding that 
				the reforms to tourism visas arrangements are aimed at improving 
				Oman’s competitiveness in attracting short-break vacationers and 
				MICE business.<br>
				<br>
				The Royal Oman Police (ROP) recently slashed the fees for short 
				term visas and also extended the free visa period from 24 to 48 
				hours, for cruise passengers and crew. Tourists visiting the 
				Sultanate for less than ten days will have to pay only RO5 
				instead of the earlier RO20. For cruise liners, there will be no 
				visa fees upto 48 hours and will be charged RO5 for extension of 
				ten days.<br>
				<br>
				HE Maitha says the focus on the MICE segment began a while ago 
				and is not a new concept because the foundation for this segment 
				is good infrastructure and this requires advanced planning. We 
				have seen an increase in the MICE business in Oman in the past 
				two years especially and there are lots of reasons for it. “The 
				geographical location of Oman, the stability, security and 
				safety all contribute heavily to the segment. Importantly, we 
				have been mapping Oman by participating globally in investment 
				forums, trade activities etc. This has supported us in 
				showcasing Oman as a preferred destination for MICE,” she adds.<br>
				<br>
				In addition, the ministry has a dedicated events division within 
				the Directorate of Tourism Promotion to lead Oman’s presence at 
				many international MICE meetings and exhibitions such as EITBM 
				(Spain), IMEX (Germany) and GITBM (UAE). Recently, the ministry 
				participated in MICE exhibitions in India and Australia.<br>
				<br>
				<b>Market dynamics<br>
				</b>The topmost criteria for selecting a city or country for 
				MICE is the destination’s appeal. With Oman receiving numerous 
				rankings in worldwide reports, the country’s appeal as a 
				top-notch destination has been sealed. Oman has been ranked 
				among the ‘20 Top Tourist Destinations in the World’ by the 
				National Geographic magazine – the only Arab country to figure 
				in the elite list. Muscat city has also been declared the Arab 
				Tourism Capital for 2012 by the Arab Tourism Ministers’ Council. 
				In another prestigious development, Muscat has been ranked the 
				second best city in the world to visit in 2012 by Lonely Planet, 
				the popular travel guide publisher affiliated with the BBC. 
				Second only to London in Lonely Planet’s list, Muscat bested 
				some of the world’s most iconic cities including Cadiz, Spain; 
				Bangalore, India; Stockholm, Sweden; Hong Kong; and Orlando, 
				USA.<br>
				<br>
				With Oman’s Ministry of Tourism aiming to increase the 
				contribution of tourism to the Sultanate’s GDP from two per cent 
				in 2011 to around 3.5 per cent in 2015 and a planned 10 per cent 
				growth for the sector in 2012, the efforts to promote and market 
				the country have been upped.<br>
				<br>
				Oman Air, the national carrier has launched a marketing campaign 
				across Europe to promote both the country and the airline. It 
				bagged the Silver Award for the Best Marketing Campaign in 2010, 
				held by the Travel Industry Club in Cologne, Germany for its 
				marketing campaign, ‘Soul of Arabia’. The airline currently 
				operates direct flights to 41 destinations worldwide, the most 
				recent addition being Zurich. Oman Air also has codeshare with a 
				number of international airlines thus increasing its 
				connectivity across the globe.<br>
				<br>
				In order to showcase the country’s image as an oasis of calm and 
				stability, an International Media Treasure Hunt was organised by 
				Oman Air in cooperation with the Ministry of Tourism in 
				February. Instead of holding a routine familiarisation trip for 
				tour operators, Oman Air launched this exciting promotional 
				event wherein journalists were given an opportunity to discover 
				the history, culture, landscapes, heritage and people of the 
				Sultanate of Oman (for full report see Page… ). <br>
				<br>
				The launch of a global awareness campaign on BBC Worldwide, the 
				first of its kind for the Sultanate; the joint efforts with Oman 
				Air and the industry to launch a stopover campaign that takes 
				advantage of Oman Air’s growing international network; the 
				campaign of one free night in Oman launched in early February 
				and the slashing of visa fees are examples of promotions.<br>
				<br>
				<b>Business meetings<br>
				</b>The OCCI is perhaps the busiest organisation in the country 
				as it hosts an average of three international government 
				delegations per week with each group consisting of a minimum of 
				10-15 members. If you take into account the hotel rooms required 
				for the stay of such international delegations as well as the 
				venues needed for conducting political and business meetings for 
				such groups and the associated economic activity and logistics 
				like shopping, restaurants and sight-seeing, the benefits are 
				immense. Little wonder then that OCCI has been working closely 
				with the Ministry of Tourism to ensure that the private sector 
				concerns are addressed.<br>
				<br>
				“I think MICE is a very positive activity in the country and we 
				should take advantage of the situation prevalent in the region, 
				highlight the peace and stability found in our country and grab 
				the opportunity that this segment presents. At OCCI, we host 
				conferences, workshops for government and private business 
				delegations that come from abroad. We don’t say no to any 
				delegation therefore we are constantly busy at the chamber all 
				through the year,” says HE Khalil Al Khonji, chairman, OCCI.<br>
				<br>
				Globally, the MICE market has attributes that make it an 
				attractive proposition for national tourism strategies. It is 
				among the highest yielding segments with many business delegates 
				bringing in partners and enjoying pre and post-conference tours. 
				Moreover, the business and incentive travel covers all economic 
				sectors and provides economy-wide benefits by way of 
				tourist-spends within the country for shopping, recreation, 
				restaurants etc. The pre and post tours spread the benefits of 
				tourism to regional and remote areas, creating jobs. Being a 
				labour intensive industry with high ‘multipliers’, tourism leads 
				to a far reaching economic impact. More importantly for the 
				Sultanate, MICE events can operate year-round, an important 
				issue for Oman that shows a large dip in summer visitation.<br>
