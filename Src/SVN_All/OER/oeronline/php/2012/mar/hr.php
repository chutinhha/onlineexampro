						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">
						NHI launches ‘Smiles of Oman’ training programme<br>
						</font><font face="Verdana" size="2">
                		<br>
						Customer care skills are an important facet for services 
						industry which this programme aims to impart to 
						employees of establishments that have constant 
						customer-employee interaction. An OER report<br>
						&nbsp;</font></b><p align="center" style="margin: 3px 10px">
						<img border="0" src="images/hr-mar-12-1.jpg" hspace="8" vspace="5" width="300" height="395"><p align="justify" style="margin: 3px 10px">
						<b>
						<font face="Verdana" size="2">
						<br>
                &nbsp;</font></b><p align="justify" style="margin: 3px 10px">
						<font face="Verdana" size="1">The National Hospitality 
						Institute (NHI) has launched ‘Smiles of Oman’, a 
						first-of-its-kind training programme to upgrade customer 
						care skills of employees working in the services sector 
						like banks, retail shops, telecommunications, hotels 
						etc. ‘Smiles of Oman’ has been conceptualised by NHI 
						after receiving numerous feedbacks about the challenges 
						faced by companies in the employee-customer interaction. 
						‘Smiles of Oman’ is expected to provide employee with 
						interpersonal skills on how to deal with customers with 
						a smile.<br>
						<br>
						“Every organisation or company which adopt our ‘Smiles 
						of Oman’ training programme for its employees will be 
						given a ‘Smiles of Oman’ logo which they can prominently 
						display in their shops in addition to badges which will 
						be given to each individual who undergoes the training,” 
						says Robert Maclean, principal, NHI, while giving 
						details of this unique training project.<br>
						<br>
						By displaying the ‘Smiles of Oman’ logo, that 
						organisation sends out a message to the customer that 
						their employees have been trained in customer care which 
						will generate a certain level of confidence among the 
						visitors. According to Maclean, ‘Smiles of Oman’ has a 
						very serious commercial value especially for 
						establishments like large retail chains as these are the 
						places where there is a lot of interface with people.<br>
						<br>
						“One of the major challenges being faced by the services 
						industry is the poor interactive skills of employees 
						which has prevented many a customer from a return visit. 
						But with ‘Smiles of Oman’ programme, the added value for 
						organisations is the fact that you are showcasing your 
						concern towards your customers by making a business 
						statement that your employees are trained in 
						professional customer care,” adds Maclean.<br>
						<br>
						NHI is the leading provider of quality vocational 
						training for hospitality, catering, and travel 
						industries in the Gulf region. It operates with the 
						clear intent of providing the most comprehensive and 
						professional training possible. While providing expert 
						training with a focus on internationally accredited 
						vocational courses, NHI continually strives to build its 
						portfolio of deliverable courses to cater to the growing 
						needs of the community. With this stated intent, NHI is 
						rolling out this unique programme for companies in the 
						Sultanate with the hope that ‘Smiles of Oman’ will set a 
						new benchmark for customer care. Maclean emphasises that 
						customer care is all about dealing with people and is 
						entirely different from the concept of customer service.<br>
						<br>
						“I hope that businesses will see our programme and be 
						inspired to develop their people on the right note as 
						the perception of a company is derived from its people’s 
						interaction with their customers. We see this programme 
						as merging customer care which deals with people and 
						customer service which deals with products. In short it 
						is all about merging people and products to give the 
						customers a better experience,” says Maclean. <br>
						<br>
						‘Smiles of Oman’ programme is tailored in such a fashion 
						as to instill confidence among the employees about their 
						skill to deal with hundreds of people with a smile and 
						it will also send across a message that people 
						interacting with customers at the till in a retail store 
						or a reception desk in hotel or bank are all trained 
						professionally. “In the long run, ‘Smiles of Oman’ will 
						lead to a better retention rate of staff as you will 
						have contented employees dealing with customers with a 
						smile on their face. This will in turn bring back the 
						customers many times over to a particular place and 
						generate more business for itself. So it is going to be 
						a win-win situation for everyone with ‘Smiles of Oman’,” 
						adds Maclean&#8194;<br>
