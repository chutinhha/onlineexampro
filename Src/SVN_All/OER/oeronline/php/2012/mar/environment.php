                </font></b>
				<font face="Verdana" size="1">
				<br>
                		Haya Water will supply treated effluent for the 
						irrigation of the Ghala Golf Club facility, adding the 
						golf facility to its growing list of customer in the 
						Muscat area base for the treated effluent produced as 
						part of the company’s ongoing water reuse project in the 
						Muscat Governorate area.<br>
						<br>
						Golf courses are already among the biggest consumers of 
						treated effluents produced by Haya Water as a wholly 
						beneficial product of the company’s ongoing world-class 
						water reuse project that will provide a state-of-the-art 
						system for home owners and businesses in the Muscat 
						area.<br>
						<br>
						The treated effluent is an environmentally friendly and 
						highly cost effective solution to large- scale water 
						users who otherwise have to depend on desalinated water 
						or potentially deplete existing groundwater supplies. By 
						using treated effluents, golf courses like Ghala Golf 
						Club and others are able to make a significant and 
						positive contribution to the environment and work with 
						Haya Water to help the company achieve its long-term 
						objective of making Muscat <br>
						a better city.<br>
						<br>
						Says Engineer Khalid al Lawati, senior area manager, 
						Haya Water, “We are delighted to finish this project and 
						add Ghala Golf Club to our growing number of customers 
						in the Muscat Governorate area who are able to benefit 
						from the treated effluent that our water reuse project 
						is producing.”<br>
						<br>
						The effluent will be provided to Ghala Golf Club via a 
						six-kilometre pipeline from Haya Water’s Al Ansab 
						treatment plant, which is largest Submerged Membrane 
						Bioreactor (MBR) treatment plant in the region. The 
						connection line to Ghala Golf Club will also provide 
						Haya Water the opportunity to supply treated effluent to 
						other customers along the Muscat Expressway.<br>
						<br>
						Says Bruce Fennessy, Ghala Golf Club’s general manager, 
						“We are delighted to have entered into this partnership 
						with Haya Water and benefit from their excellent treated 
						effluent product. Golf courses are notorious for 
						utilising a lot of water for growth and maintenance 
						which is why we are so pleased to have sourced a 
						cost-effective, value- for-money and most importantly, 
						an environmentally sustainable solution for the club. We 
						are sure that Haya Water will continue to exceed our 
						expectations and we look forward to their continued 
						support in helping to develop golf in Oman.”<br>
						<br>
