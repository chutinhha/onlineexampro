						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">
						Potential return in Schlumberger and Halliburton<br>
						</font><font face="Verdana" size="2">
                		<br>
						Technology in 2012 is not just about the Cloud in 
						Silicon Valley or genetic engineering in a biotech lab 
						but also suffuses the DNA of the oil &amp; gas business<br>
						<br>
						</font></b>
						<i><font color="#333333" face="Verdana" size="1">BY Matein Khalid</font></i><p align="justify" style="margin: 3px 10px">
				<font face="Verdana" size="1">
				<br>
				Schlumberger is the world’s leading oil field services colossus, 
				a firm that is transforming the future of energy with its 
				technological prowess in hydraulic fracturing, oilfield 
				reservoir management seismic and deepwater drilling services in 
				80 countries in all the planet’s major and emerging oil 
				provinces. Deepwater drilling is a high stakes, high risk, high 
				tech project akin to NASA’s space shuttle programme or the 
				building of the Giza pyramids in ancient Pharonic Egypt, a 
				testament to our species’ quest for the ultimate frontiers of 
				technology.<br>
				<br>
				Schlumberger, which spends one billion dollars a year on tech 
				R&amp;D, is a critical partner of all the world’s state owned 
				companies who control the planet’s oil reserves. And it sponsors 
				so many drilling projects as well as Big Oil supermajors. 
				Technology in 2012 is not just about the Cloud in Silicon Valley 
				or genetic engineering in a biotech lab but also suffuses the 
				DNA of the oil &amp; gas business. Fracking alone has exponentially 
				increased US natural gas and liquid fuel reserves/production and 
				has geopolitical implications as it will reduce America’s 
				reliance on Arab/OPEC imports and Poland’s dependence on Russian 
				gas. Schlumberger is Petrobras’s contractor in the deepwater 
				Santos Basin in Brazil, has worked with Saudi Aramco, ADNOC and 
				Kuwait Petroleum Co. for decades, and is courted by Rosneft and 
				the Kremlin as much as by the Chinese Politburo to develop 
				unconventional gas reservoirs in the Middle Kingdom.<br>
				<br>
				It is ironic that Schlumberger shares fell from 95 in July to 68 
				now even though West Texas/Brent have spiked higher due to a 
				spectrum of geopolitical black swans – Iran’s naval war games on 
				the Straits of Hormuz, protests in Moscow, the Boko Haram 
				bombings in Nigeria, the escalation of violence in Syria and 
				Yemen.<br>
				<br>
				As long as oil prices remain above $80, I can see no scenario 
				where upstream exploration drilling metrics would hit 
				Schlumberger. This is particularly true in Brazil, offshore West 
				Africa, Kurdistan, China and the Russian Arctic. The Gulf of 
				Mexico and the North Sea are now cheap call options on SLB. It 
				is entirely possible to envisage $5 in EPS in 2011 and for the 
				shares to be rerated to their previous 20X valuation range. This 
				means Schlumberger has the potential to rise to $100 a share. 
				The stock market is skeptical about the Smith International (SII) 
				takeover’s integration risk but SII is a crown jewel in the 
				brave new world of deepwater drilling and hydraulic fracturing. 
				Schlumberger is clearly a bargain in the 60-65 range for a one 
				year target of $100.<br>
				<br>
				<b>Halliburton’s potentials</b><br>
				Halliburton has huge money making potential in 2012, as it is 
				entirely leveraged to the shale oil revolution in Texas, 
				Oklahoma and the Rocky Mountain states. This means US land rig 
				counts; drilling and pressure pumping will rise; and the oil and 
				gas upstream advantage for Halliburton. HAL, is after all, the 
				number one vender of pressure pumping technologies in North 
				America. The company, freed from the malign ghost of former CEO 
				and US vice president Dick Cheney, long divested its contract 
				construction business KBR, which had razor thin margins and 
				headline risk.<br>
				<br>
				HAL has even wrested international market share from its rivals 
				Banker Hughes and Weatherford, though Schlumberger still rules 
				the roost. Halliburton has growth metrics and can easily deliver 
				25 per cent EPS gains in 2012 and 2013. Amazingly, it trades at 
				a rock bottom valuation of a mere 8.5 times forward (2012) 
				earnings and barely 3.6 times enterprise value to EBITDA. US 
				pressure pumping prices and the land rig count are bound to rise 
				as the shale oil revolution accelerates, though Mocondo lawsuits 
				are one reason why the company trades at a Cinderella valuation. 
				I am convinced that Halliburton is a compelling buy in the 30 – 
				32 range for at least a 50 target.<br>
				<br>
				<b>Advantage China<br>
				</b>Emerging markets as an asset class were a disaster in 2011, 
				as the collapse of the Sensex (and the Indian rupee) and the 
				downtrend in the GCC market demonstrated. Yet Chinese shares are 
				now trading at below 9 times earnings, a valuation that is near 
				its post-Lehman lows. China’s central bank (PBOC) will now 
				engage in an epic banking reserves ratio cut programme since its 
				CPI rate has peaked at 6 per cent. Chinese equities have 
				historically been highly correlated to easy money and liquidity 
				cycles. Unlike India, China faces no risk of major earnings 
				estimate cuts and property prices have been falling in the 
				Middle Kingdom since 2010. Of course, as export slows and Europe 
				slips into recession, China faces hard landing risk and 2012 is 
				a year of political transition as President Hu Jintao and Prime 
				Minister Wen Jiabao are succeeded by a new generation of 
				Politburo leaders. An oil shock or a property crash could well 
				lead a new wave of selling, as would a Greek default. However, 
				Chinese banks such as ICBC, Bank of China and China Construction 
				Bank demonstrate deep value. A low beta, high yield defensive 
				alternative to the Chinese banks is the New York ADR of China 
				Mobile, the world’s leading telecom. I am less enthusiastic on 
				Hong Kong shares, as I believe HSBC and the major developers 
				will pressure the Hang Seng index. If, as Den Xiaoping 
				recommended, getting rich is glorious, China’s megabanks and 
				telecoms shares at current levels definitely might help earn 
				some glory!<br>
