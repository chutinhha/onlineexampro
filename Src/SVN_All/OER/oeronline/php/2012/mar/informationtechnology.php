						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">
						Portfolio management and cost reduction</font></b><p align="justify" style="margin: 3px 10px">
						<b>
						<font face="Verdana" size="2">
                		<br>
						It is fundamental for portfolios to strike a good 
						balance between “business as usual” activities and the 
						appropriate level of growth </font></b>
						<p align="justify" style="margin: 3px 10px">
						&nbsp;<p align="justify" style="margin: 3px 10px">
						<b>
						<font face="Verdana" size="2">
						By Shatha Al Maskiry<br>
&nbsp;</font></b><p align="center" style="margin: 3px 10px">
						<img border="0" src="images/informationtechnology-mar-1.jpg" width="200" height="293"><p align="justify" style="margin: 3px 10px">
				<font face="Verdana" size="1">
				<br>
				Worldwide Companies in Oman are beginning to review their IT 
				project portfolio to cut costs and maximise value, and as part 
				of this process, some projects have been put on hold, delayed or 
				cancelled. It means that portfolio management is still at 
				infancy on the capability maturity model, and as a result, this 
				has raised questions as to whether the right decisions are made 
				at the right time with the right level of objective analysis. 
				Companies now strive to work around budget constraints and quick 
				decision making; and they have no choice but to put their 
				portfolio management to the ultimate test.<br>
				<br>
				Some of the trends I observed in Oman are absence of a 
				governance process, lack of executive support or broad 
				organisational support, scarcity of qualified resources, 
				departments working in silos, poor selection process, no 
				visibility of what is being done at an enterprise wide level, 
				loose accountability, and nonexistence of objective measures for 
				monitoring and reporting performance; hence overloading the 
				organisation’s capacity with multiple projects that are 
				mishandled and not aligned with its strategic direction. <br>
				<br>
				Due to lack of awareness on how crucial it is to manage project 
				portfolios effectively, some companies continue to impose 
				themselves with risks such as operational disruption for months 
				and even years, uncontrolled costs, conflicts of resources and 
				priorities, and ultimately fail to derive a return from its 
				investments be it in information technology, finance, HR or 
				operations. The issue is more serious for companies who do not 
				measure or monitor their portfolios carefully; hence, the 
				companies bleed considerably more; not just at an operational 
				and financial level, but also compromise the morale of their 
				workforce.<br>
				<br>
				It is fundamental for portfolios to strike a good balance 
				between “business as usual” activities and the appropriate level 
				of growth. It should also be flexible to swiftly adapt to 
				business changes because what could have promised a good return 
				at inception may no longer deliver value in the existing 
				conditions. HarmoniSation, integration and business intelligence 
				are some good remedies; and I am beginning to see a positive 
				trend whereby companies are investing in management information 
				systems for data reporting. This enables a wide spectrum of 
				valuable measures to be defined, reported and monitored so that 
				executives are empowered with reliable and accurate information 
				to arrive at swift decisions and tough calls to 
				either-go-or-kill projects.<br>
				<br>
				Companies must have three crucial goals for benefit attainment: 
				to do the right things (demand management), to reap the right 
				benefits (value management) and to do them well (programme 
				management) with accountability. It can be achieved by 
				implementing a metrics-based risk and value management approach 
				to assess current projects consistently against changing 
				business priorities and to have the ability to visualise where 
				inefficiencies exist a nd how to maximise the value of IT 
				investments.&#8194;<br>
