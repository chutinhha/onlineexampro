						<td valign="top">
						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">
						Maximising potential<br>
						</font><font face="Verdana" size="2">
                		<br>
						PDO ends 2011 with a strong showing in production, 
						reserves, safety and the employment of Omanis. An OER 
						report<br>
						&nbsp;</font></b><p align="center" style="margin: 3px 10px">
						<img border="0" src="images/oil&gas-mar-12-1.jpg" hspace="8" vspace="5" width="300" height="412"><p align="justify" style="margin: 3px 10px">
						<b>
						<font face="Verdana" size="2">
						<br>
                &nbsp;</font></b><p align="justify" style="margin: 3px 10px">
						<font face="Verdana" size="1">PDO announced that the 
						company’s combined production of oil, gas and condensate 
						for 2011 stood at 1,206,000 barrels oil equivalent per 
						day (boe/d) -- the second highest production on record 
						achieved by it. “PDO has delivered stable oil production 
						and increased gas supply consistent with its short and 
						long term objectives of providing a significant and 
						sustainable energy and revenue stream for the benefit of 
						the Omani national economy and its people,” says PDO’s 
						managing director, Raoul Restucci at the oil and gas 
						major’s annual media briefing held in Muscat recently. 
						“The high level of drilling and engineering activity has 
						paid off and, for the fourth successive year, combined 
						production of oil, condensate and gas has increased.” 
						Daily oil and condensate production in 2011 stood at 
						549,280 and 93,600 barrels per day respectively and 
						non-associated and associated gas production at 463,000 
						and 85,000 barrels of oil equivalent per day 
						respectively.<br>
						<br>
						In the greater Lekhwair area in the northeast of PDO’s 
						concession area, an exploration team made two oil 
						discoveries close to existing oilfields; Lekwhair-724 
						and Mazkhour-5. Early test results are very encouraging 
						with a combined production rate from these two wells in 
						excess of 2,600 barrels per day (bpd), says Restucci.<br>
						<br>
						“In the south of Oman, PDO exploration well Sakhiya-21 
						discovered oil in an intra-salt Ara stringer carbonate 
						reservoir near the Harweel production station. The well 
						sustained an oil flow of over 1,800 bpd during the 
						production test. The eventual recoverable volumes from 
						the field, including the use of miscible gas injection 
						as a secondary recovery scheme, are expected to be over 
						35 million barrels of oil,” he adds.<br>
						<br>
						Importantly, PDO’s gas exploration team also had a 
						successful year with yet another gas discovery in 2011 
						at Rabiha-1 in the north of PDO’s concession area. The 
						new discovery is 10 km to the southwest of the Khulud 
						South discovery announced in 2010 and 6.5 km west of the 
						Khulud West gas discovery revealed last year. Rabiha was 
						one of the four wells drilled targeting unconventional 
						gas reserves in the Haima group. The well flowed gas to 
						the surface during a flow test, and an appraisal well is 
						planned in 2012 to properly assess reservoir 
						distribution and commerciality.<br>
						<br>
						PDO also announced a successful year in terms of 
						exploration and reservoir development with a number of 
						new and potentially significant oil and gas 
						opportunities and reserves additions. While continuing 
						to make conventional discoveries, PDO’s exploration 
						emphasis in 2011 shifted to unconventional areas. 
						Restucci highlighted the emergence of unconventional 
						tight oil and basin centred gas opportunities. In 
						addition, field development and recovery optimisation 
						studies added 266 million barrels of oil reserves and 1 
						TCF of gas reserves (for a combined 450 million boe). 
						Restucci further added that “the extensive 
						inventorisation and field development work has resulted 
						in a Reserves Replacement Ratio (RRR) of 1.26, well in 
						excess of 2011 production, while the encouraging 
						unconventional results are expected to underpin 
						longer-term production opportunities after extensive 
						appraisal, testing and maturation work. “Thanks to 
						industry leading EOR work, fields like Marmul, on stream 
						for more than 30 years, were now producing at their 
						highest ever rate, while new steam projects in Qarn Alam 
						and miscible sour gas injection in Harweel, would 
						underpin key production delivery in 2012,” says Restucci.<br>
						<br>
						PDO also recorded its best ever year in terms of Total 
						Recordable Case Frequency (TRCF) safety performance and 
						has now achieved 450 million kms without a work related 
						road fatality.<br>
						<br>
						Restucci also presented PDO’s new ‘In-Country Value’ 
						strategy and the successful recruitment, in partnership 
						with its contractor community, of more than 4,300 Omani 
						nationals. More than 25 per cent of well engineering 
						contracts were awarded to Omani-owned enterprises, and 
						PDO plans to double the number of skilled Omanis 
						employed by its contractor community within the next two 
						years. In addition, PDO is working closely with the 
						Ministry of Oil &amp; Gas, OPAL and other operators to 
						sharply increase its spending on goods and services from 
						suppliers within the Sultanate.<br>
						<br>
						“PDO has recorded a successful year in 2011 despite 
						challenging market conditions and several contractor 
						work stoppages earlier in the year.” Restucci concludes, 
						“Our work this year has laid the groundwork for 
						sustained production and recovery optimisation, while 
						progressing new play opportunities that could transform 
						our longer-term delivery and contribution to the 
						Sultanate. Beyond hydrocarbons, PDO also intends to set 
						the benchmark in supporting local community development, 
						training and up-skilling for Omani nationals, adding 
						direct and indirect value to the domestic economy 
						through local service and goods manufacturing, and 
						further increasing our contribution in social 
						Investment.”<br>
						<br>
						<b>‘Together for Accident Free Oman' concludes 
						successfully</b><br>
						<br>
						‘Together for Accident Free Oman’ a month long 
						nationwide road safety awareness campaign launched by 
						Shell Development Oman (SDO) in partnership with Shell 
						Oman Marketing, Sheida International and Oman Road 
						Safety Association turned out to be a huge success. The 
						final road show concluded at Barka, Lulu Hypermarket 
						recently and was attended by almost 200 people showing 
						their strong support to the campaign. <br>
						<br>
						The road show toured all the governorates in the 
						Sultanate, starting from Muscat, Al Batinah, Al 
						Sharqiyah, Al Dakhiliyah, Al Buraimi and Al Dhofar 
						drawing large crowds across all the venues of the road 
						show which were strategically selected as per the Oman’s 
						Population and Traffic Statistics- 2010/2011 from ROP.<br>
						<br>
						“Road safety is something that concerns all of us and we 
						are delighted to be working on this national cause to 
						reduce accidents and fatalities on our roads, said Hilal 
						Al Mawali, external affairs manager, Shell Development 
						Oman. “We hope today that this successful campaign 
						helped engage the hearts and minds of the people and 
						community at large, and aimed at influencing drivers to 
						change some of their fatal behaviours like speeding, 
						talking on the phone while driving and not wearing seats 
						belts,” he added.<br>
						<br>
						The road is for all and compliance is the solution, 
						which proved to be an excellent example of embodiment 
						and distinctive partnership between government and 
						private sector and civil society institutions to 
						activate their role in bearing the responsibility and 
						contributing in raising road traffic awareness.&#8194;<br>
