						<p align="justify" class="style10">
						General Motors’ dealers in the Middle East reported 
						total sales of 10,349 vehicles in January 2012, with a 
						25 per cent increase compared to the figures in 2011. 
						The outstanding sales result represented GM Middle 
						East’s highest January sales ever as demand continues to 
						grow for the company’s line-up of passenger cars, 
						crossovers, sport utility vehicles (SUVs) and pickups 
						across the Chevrolet, GMC and Cadillac brands. The 
						strong start to the year was driven by robust sales of 
						GM’s crossover vehicles, which increased 88 per cent in 
						January with impressive sales of the Chevrolet Captiva 
						(up 226 per cent) and the Cadillac SRX (up 68 per cent). 
						Sales of SUVs grew by 46 per cent, helped by strong 
						sales of the Chevrolet Tahoe (up 67 per cent), while 
						pickups registered 33 per cent growth led by an 85 per 
						cent sales gain for the Chevrolet Avalanche and a 35 per 
						cent uplift for the GMC Sierra.<br>
