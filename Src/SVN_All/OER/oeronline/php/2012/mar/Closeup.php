						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">
						Kuwait’s economy post-elections</font></b><br>
				&nbsp;<p align="justify" class="style13" dir="ltr">
				The onus on of breaking Kuwait’s economic inertia rests on the 
				shoulders of the newly elected parliament<br>
				<br>
				<br>
				Kuwait’s new parliament, elected on February 2, must address 
				outstanding economic issues facing the nation. These include 
				taking steps to entice foreign direct investment (FDI) as well 
				as strengthening economic reforms.<br>
				<br>
				The fact that Kuwait is the smallest recipient of foreign direct 
				investments among fellow Gulf Cooperation Council (GCC) 
				countries sums up the challenge. According to the World 
				Investment Report 2011, issued by the United Nations Conference 
				on Trade and Development (Unctad), Kuwait only attracted $81mn 
				worth of FDI in 2008. By comparison, Saudi Arabia and Qatar 
				enticed some $28bn and $5.5bn, respectively.<br>
				<br>
				<b>Oil and Gas sector</b><br>
				One such challenge concerns opening up the upstream energy 
				sector. Successive parliaments have failed to endorse Project 
				Kuwait. The scheme aims to generate an additional 450,000 
				barrels per day from four fields located in north and west of 
				the country. The development requires an investment of $8bn over 
				a 20-year period.<br>
				<br>
				Traditionally, legislators had rejected the scheme citing a 
				constitutional article banning foreign ownership in the 
				hydrocarbon sector. Absence of spare capacity denied Kuwait the 
				opportunity to enhance oil production last year to help make up 
				for disruptions of supply from Libya. For its part, Saudi Arabia 
				took the lead by compensating the international market for 
				Libyan oil.<br>
				<br>
				<b>International surveys</b><br>
				Also, Kuwait is a poor performer in another key international 
				survey, namely the Global Competitiveness Index (GCI), which is 
				an integral part of the Global Competitiveness Report (GRC) 
				published annually by The World Economic Forum.<br>
				<br>
				The 2011-2012 version of the report ranks Kuwait 35 globally, 
				only better than Bahrain among GCC countries. By contrast, the 
				same report ranks Qatar at 17 globally, the best among all Arab 
				countries. GRC ranks countries on the basis of their performance 
				on numerous variables including infrastructure, macroeconomic 
				stability, labour market efficiency, financial market 
				sophistication, technological readiness, market size and 
				innovation.<br>
				<br>
				<b>Burden on state expenditures</b><br>
				Visiting Kuwait last month to better appreciate election issues, 
				it was comforting to note that economic issues formed part of 
				the debates. One proposal calls for increasing the percentage of 
				annual revenues set aside for future generations from 10 to 15 
				per cent. To be sure, Kuwait stands out among all GCC countries 
				for allocating a sizable portion of treasury income to future 
				generations for the purpose of ensuring equitable use of the 
				country’s resources.<br>
				The logic is simple and understandable, namely that of denying 
				repeated demands by lawmakers for increasing salaries and 
				benefits of public sector employees. Already, the majority of 
				Kuwaiti nationals work for the government, which places a burden 
				on state expenditures.<br>
				<br>
				Wrongly, current spending soaks up around 85 per cent of 
				budgetary spending, certainly at the expense of amount set aside 
				for development projects.<br>
				<br>
				By one account, some 92 per cent of Kuwaiti nationals work in 
				government departments and state-owned establishments. Not 
				surprisingly, migrant workers account for more than 90 per cent 
				of jobs in the private sector.<br>
				<br>
				In late 2011, the Emir of Kuwait, Shaikh Sabah Al Ahmed Al 
				Sabah, called on economic policy makers to rectify shortcomings 
				related to state budget and private sector role. The statement 
				was made in the context of global economic challenges including 
				debt problems facing the US and several EU countries. Reducing 
				dependence on the petroleum sector is another longstanding 
				challenge. Currently, the petroleum sector accounts for 90 per 
				cent of treasury income, 85 per cent of exports and 40 per cent 
				of gross domestic product.<br>
				<b><br>
				Need for reform</b><br>
				Undoubtedly, the statistics suggest that well-being of Kuwait’s 
				economy rests upon developments in the oil market, something not 
				desirable in the light of repeated upheavals. Oil prices reached 
				a record $147 per barrel in July 2008 only to plummet to below 
				$40 per barrel in early 2009 in the wake of the global financial 
				crisis. Still, average prices were expected to remain just above 
				$100 per barrel in 2011 on the back of firm demand. 
				Nevertheless, the authorities prepared the budget for 2011-12 
				with an assumed average rate of $60 per barrel, considerably 
				below prevailing market rate.<br>
				<br>
				Clearly, Kuwait has to go a long way streamlining laws and 
				business practices in order to attract foreign investments and 
				to make the economy a competitive one in the regional sphere. 
				This calls for initiating serious economic reforms in the 
				country.<br>
				<br>
				It is hoped that the new parliament will collaborate with the 
				authorities in using the oil proceeds to diversify the economy 
				away from the petroleum sector.&#8194;<br>
				<br>
