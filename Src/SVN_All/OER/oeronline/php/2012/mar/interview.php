						<p align="justify" style="margin: 3px 10px">
						<span class="style27"><strong>Fuelling Growth</strong></span><p class="style15">
				<span class="style14"><strong>Islamic finance is bound to open 
				new vistas for economic development in Oman as it will release 
				the semi frozen money and interest free deposits of those who 
				hitherto preferred not to deal with conventional banks, says Dr 
				Ali Muhyiddin Al-Qura Daghi, Qatari scholar and member of Sharia 
				Council of Ahli Bank’s Al Hilal Islamic banking services. 
				Talking to Muhammed Nafie and Turki al Balushi, he opines that 
				Islamic banks have been more resilient to the global financial 
				crisis than their conventional counterparts. Excerpts:</strong></span><p class="style15">
				&nbsp;<p class="style15" style="text-align: center">
				<font face="Verdana" size="1">
				<img border="0" src="images/interview-mar-12-1.jpg" style="border: 1px solid #808080; "></font><span class="style14"><strong><br>
				&nbsp;</strong></span><p class="style15">
				<br>
				<b>How are prospects bright for the Islamic banks at a time when 
				banks world over are confronted with or trying to stave off 
				credit crisis?</b><br>
				When the first Islamic bank came into being some 30 to 40 years 
				ago it was worth only $7mn; but now the concept of Islamic 
				finance has come of age with the total asset investment in 
				Sharia-complaint banks world over amounting to $1.25tn and in 
				some countries it continues to register a growth rate of 20 to 
				30 per cent. As you mentioned, now the conventional banks are 
				confronted with one of the severest crises in their history, in 
				the wake of global financial meltdown. The financial crisis has 
				turned out to be a commercial crisis and now has evolved to a 
				debt crisis spreading its tentacles across the Europe. 
				Bankruptcy threat looms large over several European countries 
				such as Greece, Spain, Italy and Portugal. Had it not been for 
				some measures adopted by the European Union, some of those 
				countries could already have gone bankrupt.<br>
				<br>
				The fragility of capitalism was exposed as the fallout from the 
				credit crunch and the wider economic crisis, demanding a viable 
				alternative. If commercial banks were required to share the 
				profits and losses of their clients, whether in business 
				investments or mortgages, they would be much more careful when 
				choosing which deals to finance. Viewed against this backdrop, 
				Islamic finance plays a constructive role in mitigating the 
				excesses committed by conventional banks.<br>
				<br>
				<b>What will be Islamic banks’ potential for contributing to 
				Oman’s economic development in the long run?</b><br>
				Islamic banking has immense potential for growth in Oman as it 
				will benefit the people of the country in multiple ways. It will 
				definitely assuage the religious fear and apprehension of people 
				in dealing with banks thereby opening up a plethora of 
				opportunities for investment from people who preferred not to 
				invest their money in traditional banks which they considered 
				religiously prohibited. Releasing these interest free deposits 
				and semi frozen money to be invested in Sharia-complaint banks 
				and projects will undoubtedly boost the economic growth of the 
				country. It will also create a number of job opportunities. It 
				will definitely be a win-win situation for all involved, not 
				only the banks but also the public, corporates and the nation as 
				a whole. Bringing more transparency and lesser risks to the 
				transactions, Islamic banks finance the specific products of the 
				clients unlike the conventional banks which give a common loan 
				the can be used for various purposes.<br>
				<br>
				Broadly speaking, Islamic banks can provide direct finance for 
				massive projects as capital funds through partnership (musharaka 
				and mudaraba) or they provide indirect finance through leasing (Ijara) 
				and sale contracts. It will be a big incentive for construction 
				and real-estate industry, as happened in Dubai where Dubai 
				Islamic Bank was really instrumental in the massive real-estate 
				development in the city by financing to build several firms, 
				building and other projects. This funding will be a catalyst for 
				robust economic growth and for reducing unemployment and 
				inflation.<br>
				<br>
				<b>What are the major challenges faced by Islamic banks all over 
				the world?</b><br>
				I think the new Islamic banks have a lot to learn from the 
				previous experiences of some existing players in this segment. 
				They need to properly study and understand the mistakes done by 
				some banks especially in providing Islamic products. For 
				example, going for ‘Organised Tawarruq’ instead of ‘Classical 
				Tawarruq’ was one of the mistakes done by some banks. (‘Tawarruq’ 
				in general is a transaction where one party buys some goods on 
				credit at a marked-up price and sells the same at a lesser value 
				for the purpose of getting cash). ‘Classical Tawarruq’ is the 
				traditional method whereby the financier sells the goods to the 
				customer who then disposes of the goods in the open market. In 
				‘Organised Tawarruq’ it is the financier who manages the entire 
				tawarruq process. But ‘Classical Tawarruq’ is a disciplined way 
				of transaction capable of moving the market effectively.<br>
				<br>
				However, unlike conventional banks that provide only interest 
				loans, Islamic banks offer the customers a wide range choices 
				and products such as Shira (purchase), Istijrar (leasing), 
				Mudharaba (speculation), and Musharaka (participation). Hence is 
				the widespread acceptance of Islamic banks even in non-Muslim 
				countries, especially in the western world. There are several 
				Islamic banks in the United Kingdom worth £30bn invested mainly 
				by non-Muslims.<br>
				In Europe, Islamic banks are gaining currency especially at a 
				time when the entire region is going through doldrums caused by 
				financial crisis.<br>
				<br>
				<b>How feasible will it be for conventional banks to offer 
				Islamic banking services?</b><br>
				We in Sharia supervisory bodies stipulate that conventional 
				banks seeking to open Islamic branches or windows must adhere to 
				several prerequisites and basic regulations. The management must 
				be independent and there should not be any mixing of funds. The 
				Central Bank of Oman also stipulates that the capital investment 
				should be new, and accounting separate, in addition to employing 
				independent automated software for Islamic banking products and 
				submitting financial reports for the approval of the Sharia 
				supervisory boards.<br>
				<br>
				<b>What will be you doing in Oman, in your capacity as a member 
				of Sharia council in Ahli Bank’s Al Hilal Islamic banking 
				service?</b><br>
				In Oman, we will be giving the banks the expertise and 
				guidelines and updating them about the best practices in the 
				Islamic banking. We will also issue fatwas and monitor the 
				practices of the banks and the decisions taken by them from time 
				to time. Basically, there are three types monitoring that an 
				Islamic bank or window has to go through: internal auditing from 
				within the bank, auditing by the Central Bank of Oman and 
				auditing by an external body which supervises whether the bank 
				complies with the Sharia regulations.&#8194;<br>
