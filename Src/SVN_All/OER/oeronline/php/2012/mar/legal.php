						<td valign="top">
						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">
						Executive Regulations to Income Tax Law<br>
						</font><font face="Verdana" size="2">
                		<br>
						<img border="0" src="images/legal-mar-12-1.jpg" width="150" height="225" align="right" hspace="12" vspace="3">By 
						Ashok Hariharan<br>
						<br>
						The new executive regulations to Income Tax Law comprise 
						eight parts and a total of 154 articles and will need to 
						be carefully considered by tax payers<br>
&nbsp;</font></b><p align="justify" style="margin: 3px 10px" dir="ltr">
				<font face="Verdana" size="1">
				<br>
				In June 2009, Oman issued a new tax law (Royal Decree No. 
				28/2009) which was effective from tax year 2010. The tax law had 
				provided for the Minister Responsible for Financial Affairs to 
				issue executive regulations (ERs) and decisions. Pending the 
				issue of such ERs, the prevailing regulations and decisions were 
				to continue so long as they were not inconsistent with the 
				provisions of the new law. The Ministerial Decision (MD) 
				No.30/2012 was issued on January 16, 2012 and was published in 
				the Official Gazette on January 28, 2012. The ERs comprise eight 
				parts and a total of 154 articles and will need to be carefully 
				considered by tax payers in ensuring compliance with the Income 
				Tax Law. <br>
				<b><br>
				ERs dealing with tax compliance obligations<br>
				New income tax forms</b><br>
				The ERs have prescribed 18 different forms (including tax 
				returns) for particulars to be submitted to the SGT by different 
				categories of tax payers and government bodies. These forms are 
				tailor made to collect relevant information on a timely basis 
				and is expected to speed up the assessment process. <br>
				<br>
				<b>Small companies excluded from submitting tax payers’ data, 
				returns and Financial Statements.</b><br>
				<br>
				In a significant move towards alleviating small companies from 
				the compliance requirements, the ERs, as envisaged by the Income 
				Tax Law, have spelt out the criteria for excluding submission of 
				tax payers’ data and returns. These are: Registered capital 
				shall not exceed RO20,000; Gross income realised shall not 
				exceed RO100,000; and, The average number of employees during 
				the period shall not exceed 8; employees for this purpose shall 
				include temporary employees. <br>
				<br>
				Further, tax payers are exempted from submitting financial 
				statements that are normally required to accompany the final tax 
				return if they meet the specified conditions which are:<br>
				Registered capital shall not exceed RO50,000; Gross income 
				realised during the period shall not exceed RO300,000; and, The 
				average number of employees during the period shall not exceed 
				10. <br>
				<br>
				There are additional provisions specified to allow the SGT to 
				monitor compliance with these conditions including the 
				requirement to have a formal decision issued by the SGT for such 
				exemption. Foreign companies are not eligible for the above 
				exemption.</font><p align="center" style="margin: 3px 10px" dir="ltr">
				<font face="Verdana" size="1">
				<br>
				</font><b>
						<font face="Verdana" size="2">
                		<img border="0" src="images/legal-mar-12-2.jpg" hspace="12" vspace="3" width="300" height="200"></font></b><p align="justify" style="margin: 3px 10px" dir="ltr">
				<font face="Verdana" size="1">
				<br>
				<b>Right to inspect documents and records at the tax payer’s 
				premises<br>
				</b>In a significant move aimed at strengthening compliance, the 
				ERs provide for a right to the tax officials to carry out 
				inspection at the tax payer’s premises during normal working 
				hours of the tax payer by providing 10 days prior notice. No 
				inspection shall, however, take place of documents or records 
				which relate to a period 10 years prior to the year in which the 
				inspection is made.<br>
				<br>
				<b>Tax payments in instalments and waiver from additional tax<br>
				</b>In a move to provide clarity, the ERs describe the 
				conditions to be met before the SGT may approve payment of taxes 
				in instalments. This will help tax payers who genuinely face 
				financial difficulties. The tax payer is, however, required to 
				provide appropriate guarantees. The ERs also specify the 
				conditions to be met for the SGT to exempt a tax payer from 
				additional taxes levied for delay in payment of taxes.<br>
				<br>
				<b>ERs dealing with computation of taxable income<br>
				General rules for deduction of expenses</b><br>
				In order to claim deduction for expenses, it is necessary to 
				satisfy the following conditions:<br>
				The expenses should be real (i.e not notional) and be actually 
				incurred; The expenses should be related to the tax payer’s 
				activity and should be necessarily incurred to realise the gross 
				income; The expenses should be recorded in the accounts; and, 
				The expenses should not be otherwise disallowable under the 
				Income Tax Law.<br>
				<br>
				The SG has the right to limit the deduction to the value of 
				services received by the tax payer. <br>
				<br>
				<b>Thin capitalisation rule introduced<br>
				</b>The ERs specify, amongst other things, a requirement for the 
				debt equity ratio not to exceed 2:1 before a deduction can be 
				claimed in respect of interest on loans from related parties. 
				This rule does not apply to interest costs incurred by banks and 
				insurance companies, permanent establishments (PE) of foreign 
				companies and proprietary establishments. <br>
				<br>
				In case of PE’s of foreign companies, the Head Office (HO) 
				should have obtained the loan from an unrelated third party for 
				the benefit of its PE. This condition does not apply to foreign 
				banks. In case of sole proprietory establishments, the 
				proprietor should have been obtained the loan from banks 
				registered in Oman. <br>
				<br>
				Relief in respect of HO expenses for foreign companies<br>
				The long awaited removal of the punitive provision limiting HO 
				overhead expenses to the average of the allowance given in the 
				previous three years has finally come through in the ERs. 
				Foreign companies can now claim up to 3 per cent of gross income 
				as a deduction (5 per cent for banks and insurance companies and 
				10 per cent for major industrial companies using modern 
				techniques). The limit of 5 per cent for foreign insurance 
				companies is based on the gross value of premiums collected 
				after deducting the reinsurance premiums. No deduction is to be 
				allowed for HO expenses in respect of the HO’s role in 
				supervision and control. <br>
				<br>
				<b>Treatment of finance leases<br>
				</b>In line with the Income Tax Law, the ER’s specify rules for 
				deducting expenses relating to capital assets acquired through a 
				finance lease. These are: The lease arrangement should be 
				considered as a finance lease in the accounts in accordance with 
				International Financial Reporting Standards (IFRS). <br>
				<br>
				The lessor and lessee would be considered to have a 
				lender/borrower relationship.<br>
				The lessee – and not the lessor – would be allowed a 
				depreciation deduction in respect of capital assets acquired 
				through finance lease. Interest on lease will be taxed in the 
				hands of the lessor based on interest shown in the financial 
				statements complying with IFRS. A corresponding deduction would 
				be allowed to the lessee.<br>
				<br>
				The termination of the finance lease without the lessee 
				acquiring ownership rights will be considered as disposal of the 
				capital asset. <br>
				<br>
				<b>Writing off of debts<br>
				</b>The ERs specify that no deduction will be given for debts 
				written off unless:<br>
				The debt has arisen due to transactions in the normal course of 
				a tax payer’s business, which are necessary to realise gross 
				income;<br>
				The debt should have been previously accounted in the books of 
				the tax payer; and,<br>
				The tax payer should have complied with the procedures specified 
				in the ERs for the recovery of the debt. The procedures include 
				legal proceedings for recovery. An exception is provided only if 
				the value of debt does not exceed RO1,500.<br>
				<br>
				Debts which are written off on account of any discount or 
				settlement reached with the debtor shall also be excluded from 
				the requirement of complying with legal proceedings. However, 
				this should be documented and supported. <br>
				<br>
				<b>Contributions to pension and provident funds<br>
				</b>Rules have been specified for claiming deduction in respect 
				of contributions to pension funds established in and outside 
				Oman. These include:<br>
				The pension fund set up in Oman should be independent from the 
				tax payer; its funds shall be kept separate from the tax payer’s 
				funds and invested for the fund’s own account. <br>
				<br>
				The tax payer’s contributions to the pension funds and the 
				fund’s obligations to pay the beneficiaries should be in 
				accordance with its rules and schemes. <br>
				<br>
				The tax payer should submit to the SGT copies (approved and 
				attested if the fund is set up outside Oman) of the regulations 
				and schemes of the fund, the licenses issued to it and, in the 
				case of pension funds set up in Oman, the audited financial 
				statements of the fund.<br>
				<br>
				In case of provident funds, they should be established in 
				accordance with the Oman Labour Law and registered with the 
				Ministry of Manpower in Oman. <br>
				<br>
				<b>Remuneration to owners and directors<br>
				</b>The rules in this regard are substantially in line with the 
				previous regulations and can be summarised as follows:<br>
				For any Omani joint stock company, the remuneration should be in 
				accordance with Article 101 of the Commercial Companies Law. <br>
				<br>
				For any other Omani company or proprietary establishment, the 
				owner/partner should be engaged in managing the tax payer’s 
				activity on a full time basis; his capacity as a manager should 
				be evidenced by the constitutive contract; and the remuneration 
				in whatever form should not exceed RO1,000 per month 
				individually and 10 per cent of taxable income (before deducting 
				such remuneration and brought forward losses) collectively. In 
				the case of propertiary establishments and Omani companies 
				practicing professional activities, the limit is increased to 
				RO3, 000 per month individually and 30 per cent of taxable 
				income (as described above) collectively. This 30 per cent 
				limitation was not there in the previous regulations. <br>
				<br>
				Other rules relating to remuneration including rent for property 
				provided by the sole propertier remain unchanged.<br>
				<br>
				<b>Exemptions from income tax<br>
				</b>Detailed rules and conditions have been set out for the 
				activities qualifying for the initial five year tax exemption 
				including the requirement to make necessary applications within 
				stipulated time. These are substantially in line with the 
				previous regulations. More stringent criteria have however been 
				specified for renewal of tax exemptions beyond five years. These 
				include the condition that the net profit (after deducting 
				losses) realised by the tax payer during the initial exemption 
				period should not exceed 50 per cent of the paid up capital at 
				the beginning of the initial exemption period. Essentially, the 
				regulations seek to limit additional exemptions only to those 
				entities that are not generating sufficient profits and, 
				therefore, require financial support in the form of additional 
				tax exemptions. <br>
				<br>
				The ERs also provide for equality of tax treatment for GCC 
				citizens (natural&nbsp; and juristic persons), and rule for 
				donations and sponsorship fees allowable as a deduction.&#8194;<br>
