						<td valign="top">
						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">
						A voyage around creativity<br>
						</font><font face="Verdana" size="2">
                		<br>
						TED is all about technology, education and design and 
						that was amply demonstrated by speakers at the day-long 
						TedxMuscat. Visvas Paul D Karra spoke to Eythor Bender, 
						CEO of Ekso Bionics, the designers of Ekso, the robotic 
						device to help paraplegics to walk<br>
                &nbsp;</font></b><p align="center" style="margin: 3px 10px">
						<img border="0" src="images/technology-june-12-1.jpg" width="500" height="333"><p align="justify" style="margin: 3px 10px">
				<font face="Verdana" size="1">
				<br>
				Amar, a young Omani has been paralysed from the waist down due 
				to an unfortunate mishap and has been confined to the wheelchair 
				for many years now, but that was until May 16. On that day, 
				thanks to wonders of modern technology, the young Omani’s 
				spirits were lifted up and a wide smile lit his face, as he 
				stood on his own feet literally and actually take a few steps 
				with his hitherto immovable legs. Amar was able to undertake 
				this amazing feat with the help of Ekso, a ready-to-wear, 
				battery-powered exoskeleton designed for patients with spinal 
				cord injuries and pathologies that inhibit their ability to 
				walk.<br>
				<br>
				This exoskeleton is designed and created by Ekso Bionics, a 
				research group based in Berkley, California. Ekso is basically 
				strapped over the user’s clothing and the patient doesn’t bear 
				the weight, however, as the device transfers the load directly 
				to the ground. Each Ekso can be adjusted in a few minutes to fit 
				most people with at least partial upper body strength, and can 
				be adjusted to fit one patient in minutes.<br>
				<br>
				<b>A sight to behold<br>
				</b>The electrifying moment when Amar was able to walk instead 
				of ‘wheelchairing’ himself around was witnessed by a 250 strong 
				audience which broke into a thunderous round of applause as Amar 
				began his second round of walk.<br>
				<br>
				This momentous occasion took place at TedxMuscat in mid-May at 
				the Millennium Resort in Musannah. What is more comforting is 
				that it is also a great privilege for the people of Oman to 
				become the first country outside of the US and Europe where Ekso 
				has been demonstrated to a live audience.<br>
				<br>
				“We are very proud that we could demonstrate to the people of 
				this wonderful country that we have a device that could bring 
				relief to hundreds of young and old who would otherwise be 
				confined to their wheelchairs. Ekso can transform the lives of 
				such physically handicapped people by helping them to perform 
				their basic functions all by their own by moving around like 
				normal people,” says Icelandic bionics designer, Eythor Bender, 
				the CEO of Ekso Bionics.<br>
				Bender and his team arrived in Muscat especially for TedxMuscat 
				to demonstrate how technology can change people’s lives. “It’s 
				indeed a technology that can impact the lives of 70 million 
				people in the world,” he emphasises.<br>
				<br>
				Ekso will be available on a larger scale by 2014 as it is 
				presently being used only in rehabilitation centres. So far 
				about 100 people have been able to walk using the device, which 
				is essentially a battery powered, robotic exoskeleton designed 
				to give paraplegics the option to enhance their independence and 
				freedom of movement.<br>
				<br>
				<b>Immense benefits<br>
				</b>Says Bender, “Together with hospitals and rehabilitation 
				centres, we train patients to use the Ekso in a safe clinical 
				environment. These clinics are innovative leaders in spinal cord 
				injury research and treatment and together, we are looking to 
				realise the full potential and clinical benefits that cutting 
				edge bionic exoskeleton technology can provide.”<br>
				<br>
				Since its foundation in 2005, Ekso Bionics has partnered up with 
				world class institutions, received research grants and licensed 
				technology to the Lockheed Martin Corporation. Today, Ekso 
				Bionics continues to pioneer the field of exoskeletons, 
				designing and creating some of the most forward-thinking and 
				innovative solutions for people looking to augment human 
				mobility and capability.<br>
				<br>
				Even as Bender showcased this revolutionary device at the 
				TEDxMuscat, the full-day event also witnessed other 
				inspirational stories and performances from top keynote speakers 
				who came from a wide diversity of disciplines to share their 
				innovative ideas from their respective fields and experiences.<br>
				<br>
				“Oman became the centre of creativity for a day,” says Roopesh 
				Bhatnagar and Arun Raj, the event organisers. “TEDxMuscat 
				attracted diverse talent across different sectors and industries 
				who demonstrated the power of creativity, and the crowd was 
				simply ecstatic. In fact, our audience is what makes TEDxMuscat 
				so special and we were very pleased with the record attendance,” 
				they said.<br>
				<br>
				TED is a nonprofit organisation devoted to ‘Ideas Worth 
				Spreading’. Started as a four-day conference in California 26 
				years ago, TED has grown to support world-changing ideas with 
				multiple initiatives. TED brings together the world’s leading 
				thinkers and doers to share ideas that count in any discipline – 
				technology, entertainment, design, science, humanities, 
				business, and development. The talks at the conference, 
				“TEDTalks,” are then made available to watch for free on 
				TED.com.<br>
				<br>
				Satyabrata Acharya, host and curator of TEDxMuscat adds, 
				“TEDxMuscat has already become ‘the’ annual gathering and a 
				catalyst of innovative ideas and concepts in Oman. There are 
				many people that we should thank and give credit for helping 
				make TEDxMuscat 2012 such a great success.”&#8194;</font></td>
