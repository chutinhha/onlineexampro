						&nbsp;<p align="justify" style="margin: 3px 10px">
				<font face="Verdana" size="1">
				<br>
				Oman Drydock Company (ODC) has completed successful one year of 
				the soft operation of its world-class ship repair yard in Duqm 
				which heralded a new era in Oman’s maritime industry. A gala 
				event was organised to mark this auspicious occasion and to 
				launch the new website of ODC, recently at Ritz-Carlton – Al 
				Bustan Palace, under the auspices of His Excellency Yahya bin 
				Said Al Jabri, chairman of Special Economic Zone Authority, 
				Duqm. Titled ‘Vision to Reality, A Year of Success’, the event 
				was aimed at celebrating the success of one of Sultanate’s most 
				prestigious projects, poised to make Duqm a thriving economic 
				and social development hub. It was in April, 2011 that, after 
				years of meticulous planning and construction works, ODC 
				launched the soft operations of its state-of-the-art dry dock 
				facility in Duqm, in an ambitious step towards contributing 
				Oman’s march towards economic diversification.<br>
				<br>
				HE Said bin Hamdoon Al Harthy, chairman of Oman Drydock Company 
				and Undersecretary of Ports and Maritime Affairs in the Ministry 
				of Transport and Communications, welcomed the gathering. 
				Commenting on ODC’s one year of successful soft operation, Al 
				Harthy said, “Today marks the celebration of one of the most 
				successful economic achievements in the Sultanate. ‘Vision to 
				Reality’ reflects ODC’s march towards strengthening His Majesty 
				Sultan Qaboos bin Said’s vision of diversified development and 
				inclusive growth. Within a short span of time, ODC could build 
				its unique brand in a highly competitive dry dock market by 
				utilizing local and international marketing and media channels.”<br>
				<br>
				The event also celebrated ODC’s best-in-the-class health and 
				safety standards and quality management system which enabled the 
				company to win the Occupational Health and Safety Advisory 
				Services (OHSAS) 18001/2007 certification and the ISO 9001:2008 
				certifications, by US-based ABS Quality Evaluations. Joe 
				Brincat, the ABS regional VP for Middle East and Eric Kleess, 
				president &amp; COO of ABS Pacific presented the certifications to 
				the company on the occasion.<br>
				<br>
				Elaborating on this deputy CEO, Sheikh Khalil bin Ahmed al Salmi 
				said, “The ODC has achieved one million safe man-hours which 
				enabled it to win the OHSAS certification. These certifications 
				testify to the fact that ODC has taken adequate steps to avoid 
				accident and hazardous situation using a comprehensive and 
				efficiently implemented system for the benefit of its employees, 
				clients and relevant stakeholders.”<br>
