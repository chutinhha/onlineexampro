						<p align="justify" style="margin: 3px 10px"> 
					<b><font face="Verdana" size="4" color="#C00000">Deciding 
					the best golfer</font></b><br>
						<font face="Verdana" size="2"><b><br>
						</b></font><b><font face="Verdana" size="2">The annual 
					Order of Merit recently launched by Oman Golf Committee 
					(OGC) seeks to set a proper system of rankings for the 
					rapidly growing golfing community in Oman. The first Order 
					of Merit will run to the end of June</font></b><p align="justify" class="style13">
				<font face="Verdana" size="1">
				<img border="0" src="http://www.oeronline.com/images/golfupdate-logo.jpg" style="border-style: solid; border-color: #808080; float: right;">Golf 
				is one of the fastest growing sports in the world, and over the 
				last few years, there has been a great increase in the number of 
				people taking up this sport in Oman. The introduction of three 
				grass golf courses at Muscat Hills, Al Mouj and the Ghala Valley 
				that are all designed and run by the best in the industry has 
				seen a rapid increase in the numbers of individuals playing the 
				sport.<br>
				<br>
				With the number of people playing golf on the rise, Oman Golf 
				Committee (OGC) has recently decided to initiate the Order of 
				Merit to track the progress of golfers, encourage participation 
				in tournaments, raise the level of the players and identify the 
				best golfers among a broad range of categories.<br>
				<br>
				The new Order of Merit is a system that will calculate the 
				results of competitive golfers in Oman over the season to arrive 
				at a rank per golfer. It will help them discover the best golfer 
				in the Sultanate. The Order of Merit has been established to 
				encourage and increase the level of friendly competition and set 
				a proper system of rankings for the rapidly growing golfing 
				community in Oman. During the year, golfers can secure Order of 
				Merit points by playing in their local club’s monthly medals and 
				other open tournaments. These points will be collated on a 
				monthly basis, giving the golfers the opportunity and platform 
				to view their progress.<br>
				<br>
				“We are very proud to introduce the first ever Order of Merit in 
				Oman which is designed to encourage and improve golfing 
				excellence in the Sultanate”, said OGC Chairman Ali al Bimani. 
				“The OGC is proud to be a catalyst and aid in the growth of this 
				fabulous game in Oman. Over the last few years, golf in Oman has 
				seen an increase in participation, attracting players from the 
				expatriate community and an increasing number of Omanis, keen to 
				take up the sport.”<br>
				<br>
				The first Order of Merit will run to the end of June. The 
				categories of the Order of Merit are as follows: winner of the 
				Order of Merit; leading Omani; leading lady; leading Omani lady; 
				leading senior; leading junior; top ranking for each club. Al 
				Bimani explained that this platform will also be used to support 
				the selection of future national teams as well as selection for 
				representatives of Oman at international events.<br>
				<br>
				He added that the OGC was very proud of its role in working 
				towards and encouraging the growth of golf in the Sultanate. It 
				is also proud of the development of the sport in the country as 
				it now boasts of three fully grassed golf courses as well as 
				several desert courses. The OGC is also working closely with the 
				Ministry of Tourism and other bodies to encourage golfing 
				tourism in the country.<br>
				<br>
				Furthermore, with regard to the long term plans and growth of 
				the sport, Al Bimani said, “We are also working very hard to 
				ensure that grass roots golf in Oman is developed. At present we 
				have a very strong junior Golf programme that has over 200 
				participants. Our first ladies programme was launched in 
				February 2012, the results of which are very encouraging.”<br>
				<br>
				The OGC is affiliated to the R&amp;A in Scotland who supports and 
				administers golf throughout the world and it is also certified 
				by the USGA to administer and manage all golfers’ handicaps in 
				Oman.&#8194;</font></td>
