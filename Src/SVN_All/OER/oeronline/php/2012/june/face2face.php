						<p align="justify" style="margin: 3px 10px">
						<span class="style27"><strong>Beating the odds</strong></span><p class="style15">
				<span class="style14"><strong>Todd M Dick, Vice President, 
				Corporate, Omantel, talks about the success story of the 
				corporate business unit and his vision for the future in this 
				candid chat with Visvas Paul D Karra<br>
&nbsp;</strong></span><p class="style15" style="text-align: center">
				<font face="Verdana" size="1">
				<img border="0" src="images/face2face-jane-12-1.jpg" style="border: 1px solid #808080; "></font><p align="justify" class="style13">
				<br>
				<b>You have been heading the Omantel corporate business unit 
				since late 2010. What has been your experience?<br>
				</b>I came to Omantel with a mandate to build the corporate 
				business department for the company and to become a leader in 
				the business market sector. I’ve been fortunate to have worked 
				with a number of tier one companies like Zain, Vodafone and 
				Telstra, in the US, Asia Pacific, Africa and throughout the 
				Middle East which has suited me well when I needed to understand 
				both local and international trends.<br>
				<br>
				My programme is to be not only the leader in Oman but also in 
				the region. So far our success has been enormous. In the 
				beginning of 2011, we launched the first ever Omantel Business 
				brand. With support from some of the biggest brands of Oman and 
				a brand nurturing campaign, a number of road shows, events and 
				key speaker appearances, we were selected as the number one 
				business brand in the Oman market at the end of 2011.<br>
				<br>
				Within just one year, we became the strongest business brand in 
				the market. That was also supported by a very thorough business 
				plan and a tailored business product road map for the business 
				community. So it was not only the branding campaign it was also 
				the specialized, industry specific, products and services that 
				we delivered to the market in 2011. <br>
				<br>
				The output from this is, that last year we contributed to the 
				Omantel company 13 per cent YoY growth in a market where we have 
				80 per cent mobile and 95 percent fixed market share. The 
				challenge for us is the business growth. When we have been a 
				dominant market player, most people say that you only stand to 
				lose, not to gain. You only stand to basically maintain, not to 
				grow. This is a testament to the strategy of the business to 
				deliver and to grow the Business Unit.<br>
				<br>
				<b>How did this growth come about? What are the differentiators?<br>
				</b>There are a number of reasons. First of all, our network and 
				quality of technology is sound and robust and we have the widest 
				reach of network. If you look at the mobile sites in the 
				country, we cover 97 per cent of the populace. Our 3G network is 
				by far superior. Our fixed infrastructure of 7500 kilometres of 
				fibre exceeds anyone in the market. We have international 
				connectivity with seven submarine cable landings. This not only 
				exceeds competitors in the country but most of the other players 
				in the region. What that means is that we become the carrier of 
				carriers. <br>
				<br>
				We are already getting opportunities from STC and Etisalat to 
				carry their traffic over land to the rest of the world. If you 
				look at our reach, we have a huge coast with a reach to the 
				whole Indian Ocean. The ability to divert the traffic if there 
				is any cut and ensure continuation and consistency is very high. 
				So one reason people come to us is the reliability factor. And 
				with the number of cable cuts, like those that affected Saudi 
				Arabia recently, continuation is important.<br>
				<br>
				We have very tailored and very focused sales managers to service 
				verticals with dedicated account managers to service the needs 
				of oil and gas, industrial, finance, government etc. We take a 
				bespoke approach to serve these customers that sets us apart 
				from any others in the market. For example, an oil and gas 
				company working in the desert will need software and hardware 
				designed to withstand adverse weather conditions of extreme heat 
				and cold and connectivity in far reaching remote locations, 
				while the finance industry may be looking for security and 
				redundancy as the basic necessity for their operations. <br>
				<br>
				Does the fact that Omantel is an incumbent and enjoyed monopoly 
				for a long time help you?<br>
				Competition is there for us but we don’t look at it like that. 
				We look at our own internal capability and continuously 
				challenge ourselves to deliver the most technologically advanced 
				services in the market. For me, if you rely too much on your 
				history that is where you are going to fail. Only those that 
				continue to innovate and change their business model will 
				survive.<br>
				<br>
				<b>Are the submarine cable landing stations contributing 
				significantly to your business figures?<br>
				</b>International connectivity is important for a number of our 
				clients. There are three landings around the greater Muscat 
				area, and we are building a few more in Musandam and Salalah. 
				This is beneficial to us, as it provides diversity, 
				geographically and for a number of other reasons. For example, 
				if unusual weather conditions occur and one international cable 
				gets cut and trouble erupts anywhere, we have three-four backup 
				cables. That is why so many regional players are asking for our 
				services. HSBC has located their data hosting centre here 
				because of our international connectivity and data park 
				facilities. We will be looking at strengthening data 
				opportunities in Oman and in the region. Interestingly, Qatar 
				National Bank has also decided to co-locate their facilities to 
				Oman for the same reason. The international connectivity is not 
				a core business activity but it is an important part of our 
				strategy.<br>
				<br>
				<b>What is the Oman Data Park all about?<br>
				</b>Oman Data Park brings tier 3 data services (the highest 
				grade) to Oman. It will be a full functioning data facility and 
				companies can use it for a number of benefits. For some it could 
				be co-location of data, for some perhaps, redundancy or backup 
				data, and for others it could be a full-fledged data offsite 
				facility. It comes down to a number of things – it could be 
				efficiencies, economies of scale for financing or managing that 
				data. For example, Bank Nizwa is looking to start their business 
				using Oman Data Park, they are not going to have their own data 
				servers. <br>
				<br>
				<b>What would be the role of Omantel in this?<br>
				</b>Maybe it is not a core competency of ours but we obviously 
				constantly look at opportunities to partner with the best. In 
				this play, we have an expertise in connectivity and data 
				management. Omantel already has existing tier 3 data centres 
				which are used for its own purposes. In this case, we saw an 
				opportunity to partner with 4Trust who have expertise in data 
				centre management. They have built a number of initiatives in 
				Oman so, for us it is a natural partnership that now enables us 
				to develop a commercial model and offer it to the open market.<br>
				<br>
				<b>What about the security concerns?<br>
				</b>If you look at the finance industry, security is one of the 
				significant factors for this vertical. One of the security 
				products is the MPLS internet protocol and Omantel has ISO 
				certification 27001, which is an international certified body, 
				to deliver the privacy element. The finance vertical would not 
				go forward with us if they felt that any security breach would 
				occur. So we are confident with our capability. I think the 
				greater business community would benefit from a service like the 
				Oman Data Park.<br>
				<br>
				<b>One of the growth areas is broadband. Is this a major 
				contributor for the turnover of big telecom companies?</b><br>
				Broadband and data is the play of the future, there is no 
				question about it. There are a number of products that are 
				coming into the market that are going to facilitate this, such 
				as the cloud services. Look at Apple, you have an Iphone, then 
				the Ipad and your Mac computer. With cloud, you can have all 
				your data (pictures, music etc.), on the cloud and you can pull 
				in information from any device. So the connectivity required and 
				the network required to deliver the information, the quality and 
				the speeds, are manifesting themselves through broadband. We 
				have foreseen that and we are investing in our capability. There 
				are two technologies which we are bringing to the market; the 
				first is LTE (long term evolution); and also GPON for FTTX. We 
				have installed GPON for The Wave, Muscat Hills and KOM. This is 
				the most advanced fibre speed that you can get globally and we 
				have it here. It will be available in Duqm as well.<br>
				<br>
				<b>With reference to the 13 per cent growth achieved by the 
				corporate business unit under you, does it mean that the 
				corporate unit was not functioning properly? </b><br>
				When I took over the Corporate Business Unit, Omantel already 
				had a significant market share in the Business market. This was 
				a testament to the team already in place. The 13 percent growth 
				came from an existing solid team, supported by a new business 
				plan, customized customer retention programs and a concentrated 
				effort to grow new revenue streams.<br>
				<br>
				<b>It seems that the 13 per cent growth rate of the corporate 
				business as compared to overall 7 per cent growth rate of 
				Omantel was achieved thanks to a lower base beginning. Do you 
				think there is more potential?</b><br>
				It is certainly going to be challenging having such a 
				significant market share. You are not going to get much growth 
				out of the existing products and base, but again, we are always 
				looking at innovative ways to grow the business. Data services 
				are one area for growth as are tailored customer solutions. 
				There are other new areas that can potentially grow new revenue 
				streams including managed services where companies want to 
				outsource their non-core services.<br>
				<br>
				<b>Your competitors had a headstart because they realised that 
				this is going to be the wave of the future. Do you think that 
				you are trying to catch up lately with them or are you already 
				there?</b><br>
				Actually, I think that our competitors are trying to catch up 
				with us. I think there was an expectation that Omantel Business 
				would have to struggle in 2011 because of our market dominance, 
				but at the end of last year, we grew. We probably have a port-in 
				to port-out ratio of 4:1. Again, with an 80 percent market share 
				that is a fantastic statistic. We didn’t have any significant 
				fixed customers leaving us last year which is a very good 
				result.<br>
				<br>
				<b>Has Omantel business been able to cope up with the demands of 
				the customers?</b><br>
				Oman is a large country with a small scattered populace and that 
				is one of the biggest challenges. Many of the cities are 
				separated by vast distances and the terrain is also rocky and 
				construction is challenging. So the cost of installing 
				infrastructure requires much more capital. To keep pace with the 
				needs of customers, Omantel has had the foresight to introduce 
				the most advanced technologies, to cut costs and speed up 
				network installations. In the last three years we invested in 
				excess of RO300m for infrastructure alone.<br>
				<br>
				<b>Omantel has recently embarked on Omantel Business Alliance 
				initiative. What sort of value you are willing to bring out of 
				this initiative to your corporate customers?</b><br>
				We recently launched the Omantel Business Alliance, a forum that 
				brings together professionals from Oman’s business community and 
				seeks to recognize and foster leadership. Through Omantel 
				Business Alliance, our aim is to connect with some of the best 
				and brightest business minds who significantly impact our world.<br>
				<br>
				At our launch event, we invited Randi Zuckerberg, the former 
				director and head of marketing, Facebook, as the guest speaker. 
				Randi was a key force behind one of the world’s greatest brands 
				today leading marketing at Facebook for six years.<br>
				<br>
				The guests at our first Business Alliance conference had a 
				thought-provoking and interesting session, on how social media 
				has revolutionized marketing strategies the world over. Randy 
				spoke more specifically about how some of the world’s leading 
				multinationals are growing and interacting with their customer 
				base through this communication medium. We hope that Oman 
				organisations will benefit from the experiences and insights of 
				the luminaries we invite. There are many more exciting and 
				inspiring events in store, which we hope to unveil in the months 
				ahead.</td>
