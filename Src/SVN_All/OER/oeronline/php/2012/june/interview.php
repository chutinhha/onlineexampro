						<td valign="top">
						<p align="justify" style="margin: 3px 10px">
						<span class="style27"><strong>For a better tomorrow</strong></span><p class="style15">
				<span class="style14"><strong>Hussain Ali AbdulHussain, CEO, 
				Haya Water discusses the company’s achievements, plans and 
				challenges in an interview with Mayank Singh and Visvas Paul D 
				Karra</strong></span><p class="style15">
				&nbsp;<p class="style15" style="text-align: center">
				<font face="Verdana" size="1">
				<img border="0" src="images/interview-june-12-1.jpg" style="border: 1px solid #808080; " width="500" height="435"></font><span class="style14"><strong><br>
				&nbsp;</strong></span><p class="style15">
				<b>You have joined Haya Water recently; what are your plans for 
				the company? <br>
				</b>I joined Haya Water as chief executive four months back and 
				we have a lot of ongoing projects underway as part of the 
				long-term water reuse project, especially in the Bausher and 
				Seeb areas and these are highly critical projects for us. In 
				Seeb we have one Sewage Treatment Plant (STP) project, with a 
				capacity of 80,000 cubic metre per day and five network 
				projects. The focus at this moment is to get these projects 
				completed by the beginning of next year and gradually finish all 
				of them by the end of 2013. Once the Seeb project gets completed 
				– which is one of the largest individual projects we are 
				undertaking - it would improve our connectivity from the current 
				20 per cent to having almost 50 per cent of homes and commercial 
				premises connected to the system across the Muscat governorate 
				area. Meanwhile we have four projects in the Bausher area and 
				one is almost complete while the other three will get completed 
				sequentially in 2012, 2013 and 2014. Once these are completed 
				the number of properties connected to the system will increase 
				to 60 per cent.<br>
				<br>
				<b>The Seeb and Bausher projects are Haya Water’s immediate 
				priority, what is in store after these get completed?</b><br>
				The roadmap for our overall water reuse project is to achieve 80 
				per cent connectivity in the Muscat Governorate area by 2018. 
				After Bausher and Seeb we will focus on Al Amerat, Matrah, 
				Quriyat and Muscat in that order. We have started planning for 
				the new projects – the Al Amerat projects are in the tendering 
				stage. In Matrah, we are working with Muscat Municipality for 
				the rehabilitation of the area as this is a congested place and 
				requires proper planning. Then we are planning for Quriyat, 
				Muscat and Sidab. We are also working on our plan to expand our 
				Al Ansab Plant and relocate the tankers discharge plant to a new 
				location.<br>
				<br>
				<b>What is Haya doing to reduce the inconvenience caused to 
				people by it’s constant digging?<br>
				</b>Any major civil engineering and infrastructure project of 
				this nature that involves connecting individual properties to a 
				modern waste water system cannot happen without there being some 
				short-term inconvenience for individuals. We are also operating 
				in one of the most challenging urban environments in the Gulf 
				with congested areas, narrow streets and the need to connect a 
				much older city to the system. This means that our contractors 
				often have to work very close to hazardous areas, because of 
				electrical cables and septic tanks. Some of these tanks are very 
				weak so they tend to collapse while the excavation is going on. 
				And in certain areas the water table is very high, so halfway 
				through the digging we find water. As a result of these 
				challenging construction issues we do receive some complaints 
				and of course individuals have every right to complain if they 
				have concerns. And one problem we face is that some of the older 
				septic tanks are actually inside the properties as I found 
				recently on a trip to Wadi Adai where our contractors were 
				working. So naturally if there is someone digging within the 
				premises of your house there is bound to be some uncomfort.<br>
				<br>
				We have a call centre with a toll free number which works from 
				6am to 10pm. The centre receives any complaints and we follow 
				this up with site visits to address such issues. I personally do 
				site visits and speak to people and most of the time I have got 
				very positive feedback for the work that we are undertaking. For 
				example in Seeb we are also working in commercial areas and if 
				you are digging a trench in front of someone’s shop, the entry 
				of customers gets restricted. I was in Seeb recently and spoke 
				to a few shop owners; all of them said that the digging was a 
				bit of a distraction, but they had no issues with it and were 
				happy to get our service. There are also many people living in 
				areas like Baushar, Al Ilham and Qurum where people are asking 
				us when we can start the project in their areas and connect 
				their homes. But one of the benefits of our construction work 
				has been our ability to use the same trenches and tunnels to lay 
				fibre optic cables – thus ensuring that a second round of 
				digging will not be required for this service in the future.<br>
				<br>
				<b>Will the delay in the current phases lead to sequential 
				delays in the rest of the project?<br>
				</b>In a project of this scale and complexity – it is the first 
				one of this magnitude in the GCC – it is very challenging to 
				always keep exactly to schedule. Due to a number of unforeseen 
				circumstances we have fallen a little behind schedule this year, 
				but once we have the Seeb catchment area on line it will bring 
				us back on schedule. And in a project of so many years we do 
				have the opportunity to accelerate the remaining part of the 
				project and catch up with the planned schedule. The delays 
				happen due to various reasons – one is the lack of underground 
				information from different authorities, obtaining NOCs from 
				different authorities and the complication of site conditions.
				<br>
				<br>
				<b>Why does it take a long time to get the requisite clearances 
				for your projects? <br>
				</b>The sheer scale and size of our project means that we need 
				to work with an enormous number of different organisations to 
				ensure we can complete the construction we need to undertake. It 
				is a huge challenge; each government organisation has its own 
				mission and targets, for example Muscat Municipality is building 
				roads and this affects our work. Then there are electricity and 
				telecom companies which have to run their cables and we have to 
				get permissions from them. There are other organisations that we 
				need to coordinate and obtain NOCs. So therefore it can 
				sometimes be a highly complex process we need to go through to 
				secure the permissions we require. <br>
				<br>
				<b>Why were STP projects given to companies which lack such 
				expertise?<br>
				</b>A highly specialist project of this nature has never been 
				undertaken in Oman before – which naturally means that the local 
				expertise will not necessarily be on the ground. But there is a 
				wealth of very experienced professional civil engineering 
				companies with experience of large scale infrastructure 
				construction work. We are also seeing a lot of local companies 
				entering joint ventures with international firms to deliver the 
				project on our behalf. In Seeb one project is a joint venture 
				between an Egyptian and an Omani company and another one is a 
				joint venture between a Kuwaiti and an Omani Company. In Bausher 
				there is a Portuguese company working with a local entities; the 
				Darsait STP is being worked upon by a Spanish and Omani company. 
				The local companies have also learnt a lot by working with 
				others, and slowly we are taking on a lot more local companies 
				on their own.<br>
				<br>
				<b>What is Haya Water’s revenue model? <br>
				</b>We do generate our own revenues but we are still dependent 
				on the subsidy from the government. At the moment the split 
				between our own revenue and the government subsidy is around 
				50:50. We hope that over the years and as the Seeb projects get 
				underway we will generate more revenues. We are producing 75,000 
				cubic metric cubes of treated effluent per day and we sell 75 
				per cent of our treated water. Our main customer is Muscat 
				Municipality who use the water for irrigating parks and other 
				public areas and we also supply water to Muscat’s three grass 
				Golf Courses. Overall, the water reuse project is still within 
				the allocated budget of RO1.8bn that will cover the whole of the 
				Muscat governorate area.<br>
				<br>
				<b>Can you give us a brief on your professional background?<br>
				</b>I graduated from the US in 1987 with a degree in mechanical 
				engineering from the University of Arizona. I spent 14 years in 
				PDO and grew within the organisation working in various 
				disciplines like planning, project management, technical etc. I 
				moved to Oman Oil Company as a technical manager and shifted to 
				Oman Petrochemicals Industries Company which was a joint venture 
				between the government and Dow Chemicals. I was the government 
				representative in the venture. I then moved to Oman Refineries &amp; 
				Petrochemicals Company as the general manager for business 
				development and marketing. </td>
