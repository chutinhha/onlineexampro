						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">
						Strategy and value in oil and gas markets<br>
						</font><font face="Verdana" size="2">
                		<br>
						As long as there is no military conflict in the Gulf, 
						Brent is overpriced at $115 and has potential downside 
						to $105-108<br>
						<br>
						</font></b>
						<i><font color="#333333" face="Verdana" size="1">BY Matein Khalid</font></i><b><font face="Verdana" size="2"><br>
                		</font></b><font face="Verdana" size="1">
				<br>
						It is ironic that Brent crude soared by $25 since its 
						recent $90 low in June even though Europe is on the 
						brink of recession and Asian industrial demand continues 
						to plummet led by the GDP slowdowns in China and India. 
						Geopolitical risks in Syria have escalated and the oil 
						market also expects a supply shock if Israel-Iran 
						tensions lead to a war, the reason the Brent one year 
						forward curve has steepened. The collision between a US 
						Navy Fifth Fleet destroyer and an oil tanker in the Gulf 
						has only exacerbated the nervousness in the physical oil 
						markets, particularly as sanctions have led to a 20 per 
						cent fall in Iranian exports.<br>
						<br>
						Supply shock risk has also been amplified by disruptions 
						in the Iraqi pipeline networks after disputes on the 
						Petroleum Law with the Kurdistan Regional Government. 
						Maintenance shutdowns in North Sea oilfields and BP’s 
						seasonal pipeline network shutdown have also led to 
						buying in the Brent-West Texas spread and global hedge 
						funds have gone long on oil futures since they expect 
						the Bernanke Fed and the Draghi ECB to lean towards easy 
						money to hedge global deflation risks.<br>
						<br>
						The oil market is also convinced that Saudi Arabia will 
						restrain its own output since the kingdom needs a 
						breakeven price above $100 to implement its $138 billion 
						public spending and social welfare programs. As long as 
						there is no military conflict in the Gulf, Brent is 
						overpriced at $115 and has potential downside to $105 – 
						108. At these levels, investors could well consider 
						strategic trades in the world’s leading oil service 
						colossus in the 64 – 66 range or Occidental Petroleum 
						(an equity stakeholder in the Dolphin Project), a major 
						operator in Qatar, Iraq, Abu Dhabi and Libya. A far less 
						volatile value share is French supermajor Total SA, 
						whose depository receipts (ADR) in New York trade at 
						seven times earnings and offer a 5.62 per cent dividend 
						yield. After all, the ten year US Treasury note yields a 
						mere 1.60 per cent and, unlike Total, does not offer 5 
						per cent growth. Caveat, I believe Brent is currently 
						overpriced and would only consider energy shares if 
						Brent falls to $105 – 108.<br>
						<br>
						Halliburton has been one of the world’s most 
						controversial oil and gas service companies in the past 
						decade, since its former CEO was former US Vice 
						President Cheney and its Kellogg Brown Root (KBR) faced 
						lawsuits related to inflated contracting overruns for 
						the Pentagon in Iraq. Yet Halliburton is now a pure play 
						oil and gas service firm as it long spun off KBR in an 
						IPO. It trades at a valuation multiple of 10, almost 50 
						per cent below its rival Schlumberger and has huge 
						growth potential in reservoir management, deepwater 
						drilling and natural gas outside the US. This is the 
						reason Halliburton moved its global head office from 
						Houston to Dubai. I believe Halliburton shares, if 
						purchased at 28 – 30, have 50 per cent upside in the 
						next twelve months as Wall Street rerates its embryonic 
						international business.<br>
						<br>
						Apache Petroleum (symbol APA in New York) is 
						unquestionably one of the world’s most attractive and 
						misunderstood oil/gas exploration and production 
						companies, with concessions and assets in offshore North 
						Sea, Egypt, Argentina, Texas, Alberta Texas, Kenya and 
						South America. Apache shares have fallen from 112 last 
						February to 85 now on the NYSE as Wall Street is 
						extremely nervous about nationalisation risk in its 
						Egyptian assets, one fifth of its proven reserves of 
						three billion oil equivalent barrels (BOE).<br>
						<br>
						However, post Mubarak Egypt desperately needs foreign 
						exchanges as central bank reserves are only four months 
						import cover. Nationalisation risk in Egypt is simply 
						not credible, though taxes and royalties on Apache’s 
						concession could well be renegotiated, as is routine in 
						the emerging markets. Apache can well deliver 15 per 
						cent production growth, thanks to its unique onshore 
						production US/Canada and global exploration portfolio. 
						The biggest risk is in Argentina, where the Peronist 
						government nationalised Repsol’s local subsidiary YPF.<br>
						<br>
						Argentina is a marginal franchise but Egypt is mission 
						critical to the future production/reserve growth for 
						Apache. President Morsi’s technocratic Cabinet has 
						indicated that foreign oil and gas companies that are 
						long term operators are welcome to develop Egypt’s 
						energy resources, a vote of confidence in Apache. Apache 
						is a beauty in the 76-78 range as its valuation 
						multiples do no justice to its EPS/reserve replacement 
						potential. Apache is also a takeover candidate for a Big 
						Oil supermajor eager to boost economies of scale in its 
						reserves/production portfolio. In that case, I doubt if 
						the Apache will go below $112. This is a risk reward 
						calculus that is compelling as I drill for oil on Wall 
						Street!<br>
