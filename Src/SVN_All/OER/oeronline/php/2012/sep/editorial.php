				<b>
				<font face="Verdana" size="4" color="#C00000">Outside to inside&nbsp; </font></b><p align="justify" class="style13">
				Crystal gazing in the field of IT can be a bit foolhardy, as its 
				landscape changes rapidly. Undeterred, I am putting out a set of 
				trends that are likely to dominate the IT landscape over the 
				next few years.<br>
				<br>
				<b>Cloud computing:</b> There are essentially three types of 
				clouds; the full Internet cloud or public cloud, the private 
				cloud, and the hybrid cloud – a mix of public and private 
				clouds. Cloud computing is picking up steam and is here to stay, 
				thanks to its competitive pricing and the ease it offers <br>
				<br>
				<b>Big Data:</b> Big Data is about bringing together structured 
				internal data that your company has always used and mixing it 
				with unstructured public data like social media streams and 
				freely available government data. The combination of these two 
				types of data can throw up new insights and that’s why Big Data 
				is such a big deal <br>
				<br>
				<b>Social Media:</b> People all over the world are using social 
				networks and social media-based services to stay in touch, 
				communicate, and collaborate. Companies that wish to remain 
				relevant in future need to devise ways to harness the potential 
				of social media<br>
				<br>
				<b>Consumerisation of IT:</b> Consumers are setting the pace for 
				innovation in technology. As a result enterprises which don’t 
				steadily consumerise their application portfolios will find a 
				lower level of adoption and usage<br>
				<br>
				<b>PC/Mobile convergence:</b> Mobile devices like smart phones 
				and tablets are able to do the tasks of a full PC. Still, there 
				are times when workers can be more productive by working with a 
				full keyboard and mouse. And that’s the reason why the lines 
				between traditional PCs and mobile devices continue to blur<br>
				<br>
				OER’s cover story on IT usage shows how Oman is adopting these 
				trends, while some of these may be in their infancy others are 
				fairly well entrenched. Plus, it also has an opportunity to 
				leapfrog other countries both in the region and internationally. </td>
