						<td valign="top">
						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">
						Felicitating excellence<br>
						</font><font face="Verdana" size="2">
                		<br>
						Graham Flannery, Director Business Development, Travelex 
						Europe and Middle East speaks to OER about the 
						forthcoming Travelex Oman Tourism Ambassador Awards<br>
                &nbsp;</font></b><p align="center" style="margin: 3px 10px">
				<font face="Verdana" size="1">
				<img border="0" src="images/tourism-sep-12-1.jpg" width="400" height="300" hspace="8" vspace="3"></font><p align="justify" style="margin: 3px 10px">&nbsp;<p align="justify" style="margin: 3px 10px">
				<font face="Verdana" size="1">
				The Travelex Oman Tourism Ambassador Awards Ceremony, a black 
				tie event is to be held in November 2012 in Oman. The call for 
				entries will take place in early September 2012, which will 
				leave enough time for the panel of judges to assess and critique 
				the finalists. The function will be attended by 200 guests and 
				the awards are expected to become an annual event acknowledging 
				and felicitating successful ‘Ambassadors” for tourism in Oman.<br>
				<br>
				The aim of this award is to celebrate individual excellence in 
				the tourism industry and reward people who are often the unsung 
				heroes. It is also an effort by Travelex to give back something 
				to the tourism industry in Oman on the occasion of completing 10 
				years in the Sultanate. <br>
				<br>
				<b>Do you have any partners for the ‘Travelex Oman Tourism 
				Ambassador Awards’. If yes, what kind of value are they bringing 
				to the table?</b><br>
				We are very lucky that the Travelex Oman Tourism Ambassador 
				Awards has got such positive support from our partners – the 
				Ministry of Tourism, Omran, Muscat Municipality, Oman Airport 
				Management Company and HH Sayyid Faisal Al-Said. Our partners 
				helped us to shape the award categories and criteria and will 
				each have a representative on the judging panel.<br>
				<br>
				<b>What is the criteria on which the candidates will be judged 
				for the award? </b><br>
				The judges will select winners based on the following criteria:<br>
				Personal initiative – giving additional advice/recommendations 
				to tourists<br>
				Going the extra mile – service over and above the normal 
				requirements<br>
				Friendliness – a winning smile and positive personality<br>
				Delivering on the promise <br>
				How the individual promotes Muscat, Oman and their local region
				<br>
				Knowledge and information <br>
				Innovation and originality<br>
				<br>
				<b>What are the main objectives behind instituting such an award 
				in Oman and how is it going to contribute to the Sultanate’s 
				tourism growth?</b><br>
				Tourism makes up a significant proportion of Oman’s economy and 
				its popularity, but there are some key people and companies that 
				truly have a positive impact on the country’s tourism sector. 
				Travelex, as an international company, has a responsibility to 
				give back to the communities in which we operate. In Oman, it’s 
				timely that the government has outlined that tourism is a 
				critical part of its growth strategy and is aligned with our own 
				growth agenda. As part of the tourism industry ourselves, we 
				felt that these awards are an ideal way for us, alongside our 
				partners, to honour those who are leading the way.<br>
				<br>
				<b>Does Travelex have a similar award elsewhere in the region 
				and is the same being replicated in the Sultanate? </b><br>
				Not for now. Oman is our first venture into these awards but 
				there is the potential for us to roll similar initiatives out in 
				other GCC countries. We chose Oman, because it was the first 
				country we entered in the Middle East back in 2003, it is a 
				truly beautiful place with equally beautiful people, and because 
				of the clear strategic vision the government has set for the 
				industry.<br>
				<br>
				<b>How do you want to build on this campaign/ award in the 
				future and what are your plans to promote these ambassadors in 
				the society? </b><br>
				Our intention was to set something up this year, as we approach 
				our tenth anniversary in Oman, which would have the potential to 
				grow each year. The encouragement we have had from the industry 
				and particularly from our partners, has been beyond our 
				expectations, so we are absolutely thrilled to get off to such a 
				positive start. What we hope the awards will achieve is a level 
				of reward and recognition for the ambassadors and the fantastic 
				work they do. By giving them some exposure to the wider 
				community, we hope this will encourage the rest of the industry 
				to step things up a notch to further promote the tourism sector 
				in Oman, both internally and externally.<br>
				<br>
				<b>According to you, what kind of an impact does a strengthening 
				Omani rial against the pound and the euro have on Oman tourism 
				industry?</b><br>
				While no one knows what will happen in the currency markets, 
				we’d hope that the strengthening of the rial won’t adversely 
				affect the inbound tourism market, particularly from the UK and 
				Europe, against whose currencies the value of the rial has grown 
				in the past year. What it does mean though is that companies may 
				need to think differently. It certainly will require all 
				businesses to maintain competitive and stay focused on and 
				ensure, in what are economically difficult times in many parts 
				of the world, that good value is still delivered. They will need 
				to maintain their competitiveness to continue to attract the 
				price-sensitive inbound tourists. However, if Oman continues to 
				innovate its tourism product - as it has done - and invests in 
				infrastructure - as it is doing with the construction of the new 
				airport and road system – we believe that Oman will continue to 
				attract an increasing number of tourists even if the rial 
				continues to strengthen. In fact, despite the recent gains, 
				passenger numbers in the past twelve months have increased, is a 
				testament to the attraction of the country.<br>
				<br>
				<b>What are the challenges you are facing especially when 
				travellers are much more likely to rely on credit/debit cards 
				instead of buying currency in advance?</b><br>
				We know that plastic has become a hugely popular method of 
				payment for travellers in recent years, but despite this, there 
				is currently more cash in circulation worldwide than there has 
				been at any point in history. As currency experts, we’d always 
				advise customers to take at least some destination currency with 
				them when they travel internationally. When you arrive late at 
				night, there may not be a currency exchange store open, ATMs may 
				not be readily available so it’s always good to have some local 
				currency on hand for incidentals like a drink or taxi.<br>
				<br>
				Like Oman’s tourism industry, Travelex also has its own growth 
				strategy, which has seen it achieve record results in what have 
				been some of the most challenging times the global travel market 
				has ever seen. We have been innovating and embracing new 
				technologies, which has seen huge growth from our ATM business. 
				Online sales of currency can make up to 30 per cent of turnover 
				in our developed markets. We recently launched our first new 
				website in the region in Bahrain, with Oman’s first online 
				currency reservation service due by the year end. Further, 
				Travelex is one of the largest sellers of prepaid currency cards 
				globally. We hope to bring these products to the Middle East 
				very soon.<br>
				<br>
				<b>Can you give us a brief about Travelex Oman and its 
				operation?</b><br>
				Travelex Oman currently has six stores, which we have run since 
				2003 in conjunction with our joint venture partner – four in 
				Muscat International Airport, and another two in the Ghubrah and 
				Seeb areas. We are due to open a new location in Barka later in 
				September and will continue to look for new locations both in 
				the capital and other regions of Oman. We provide foreign 
				exchange, cash-to-cash and bank transfer solutions.&#8194;</font></td>
