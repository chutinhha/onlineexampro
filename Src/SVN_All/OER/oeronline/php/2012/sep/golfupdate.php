						<font face="Verdana" size="2"><b><br>
						</b></font><b><font face="Verdana" size="2">Muscat Hills 
					Golf &amp; Country Club, Oman, and BankDhofar are all set to 
					host the 1st Pro-Am Golf Tournament in the Sultanate and OER 
					is excited to be an integral part of the event as its media 
					partner</font></b><p align="justify" class="style13">
				<font face="Verdana" size="1">
				<img border="0" src="http://www.oeronline.com/images/golfupdate-logo.jpg" style="border-style: solid; border-color: #808080; float: right;"></font>Muscat 
				Hills Golf &amp; Country Club has announced the launch of the 
				Inaugural BankDhofar Pro-Am Tournament. The Pro-Am event will be 
				held on Oman’s first grass championship golf course on November 
				7 and 8, 2012. This will be the first golf event featuring 
				professional golfers to be staged in Oman. The event is expected 
				to assist in the ongoing development of the game of golf 
				domestically as well as increasing Oman’s growing reputation in 
				the GCC Region as a first class golfing destination. The event 
				hosts and organisers are anticipating this event as a stepping 
				stone to larger professional golf events in the future Oman.<br>
				<br>
				“Organising and hosting such a high profiled event is part of 
				our ongoing strategy to grow the game of golf in Oman and raise 
				the profile of Oman as a great golf destination,” comments Ray 
				Stopforth, Muscat Hills Director of Golf.<br>
				<br>
				BankDhofar Pro-Am Tournament will be played over 18 holes on 
				November 8 in a Team alliance format, with the professionals 
				also competing for individual honours. November 7 is dedicated 
				for competitors practice and to enjoy a fun filled auction 
				dinner in the evening. The field will be made up of 25 teams 
				featuring a regionally based professional along with 3 amateur 
				players in a team. The BankDhofar Pro-Am prize fund is over 
				RO1,000 and will be competed for by the professional players, 
				while the amateurs will compete for team prizes.<br>
				<br>
				The launch of this high-profile event would not have been made 
				possible without the sponsorship of BankDhofar, a leading bank 
				in Oman. They have recognized the business values and social 
				ethics in the game of golf and saw a great opportunity to 
				communicate its services in front of the local golf business and 
				community. Tony Mahoney, CEO BankDhofar commented, “We are 
				delighted to participate in the 1st Pro-Am golf Tournament in 
				the Sultanate of Oman. One of our strategic goals is to get more 
				involved in the development of Omani youth. I believe by 
				BankDhofar participating in this event we can spread the growth 
				of the sport and hope that one day we will have an Omani on the 
				professional tour circuit.”<br>
				<br>
				Audi Oman is the other big sponsor who will support the 1st 
				Pro-Am in Oman as a Gold sponsor and an Official Carrier. 
				Wattayah Motors, official dealer of Audi in Oman, has already 
				proven itself as a company supporting sporting events in Oman. 
				After the successful Audi Quattro Cup in March 2012, the luxury 
				car maker continues to promote the elite sport of golf. The 
				company aims at supporting people who are pursuing their vision, 
				in areas that provide fresh impetus.<br>
				<br>
				Additional sponsors of the event are Radisson Blu Hotel, Muscat 
				– Hospitality Partner and Signature magazine as the luxury media 
				partner and OER as the media partner.<br>
				<br>
