						<td valign="top">
						<p align="justify" style="margin: 3px 10px">
						<span class="style27"><strong>Focused, hands-on investor</strong></span><p class="style15">
				<span class="style14"><strong>Al Anwar Holdings is proud of what 
				it has achieved during the last year and looks forward to a 
				promising future. At the same time, the company keeps its eyes 
				set on the tasks and goals ahead, says Reji Joseph, CEO, in an 
				interview with Visvas Paul D Karra<br>
&nbsp;</strong></span><p class="style15" style="text-align: center">
				<font face="Verdana" size="1">
				<img border="0" src="images/face2face-sep-12-1.jpg" style="border: 1px solid #808080; " width="500" height="309"></font><p align="justify" class="style13" dir="ltr">
				<br>
				<b>Can you briefly comment about when you joined Al Anwar and 
				how has your experience been in Al Anwar?</b><br>
				I joined Al Anwar Holdings a year ago during the middle of last 
				year. Previously I worked with some of the large investment 
				banks, accounting firms and corporations in the Middle East and 
				South East Asia. Most recently, I was the Director at KPMG 
				Corporate Finance, Qatar.<br>
				<br>
				I have been able to work at a rapid pace to assess and garner 
				enough information to make my initial decisions. This is because 
				of the strong and competent board we have at Al Anwar. My work 
				strategy involves a thorough understanding of the business in 
				operation, as well as the people responsible for its efficient 
				functioning. At the Al Anwar group, it was refreshing to note 
				that the strength of the management as well as the determination 
				and the passion of the employees makes this group great.<br>
				<br>
				<b>NAPCO and Voltamp are two of the big success stories of Al 
				Anwar. Do you plan to continue with the same formula for future 
				investment cycles?</b><br>
				NAPCO and Voltamp were excellent deals. More recently we did a 
				few interesting transactions; and this included our successful 
				sale of a major stake in Sun Packaging and our stake increase in 
				Taageer Finance. This is in line with our strategy. We also have 
				Al Maha Ceramics in our group which has been doing well. Al 
				Anwar is dependent upon the performance of its investments 
				during the holdings period by share of profits and dividends; 
				and at successful exits. The businesses within our group are 
				part of a very dynamic and competitive market.<br>
				<br>
				We have carved out a niche in each of the businesses and we 
				continue to track the frequent changes in technologies and 
				shifts in market dynamics. At the Al Anwar Holding group we stay 
				responsive to market signals and view each shift as an 
				opportunity. We believe that we have developed our capability to 
				constantly relook at our processes, develop new products and 
				enter newer markets. Our track record has given us the ability 
				to anticipate the changing preferences of our customers by 
				understanding the industry trends and visualising new markets. 
				We maintain our operational focus in the business we operate in 
				to optimise our participation.<br>
				<br>
				<b>What are the other companies that Al Anwar is presently 
				involved in? </b><br>
				In Oman we have investments in two listed entities namely 
				Taageer Finance Company and Voltamp Energy and three unlisted 
				companies namely Falcon Insurance Company, Al Maha Ceramics and 
				Sun Packaging Company. We have a few regional investments in 
				Bahrain, Kuwait and India.<br>
				<br>
				<b>What is the basis on which your disinvestment policy works?</b><br>
				Our investment policies which are based on sound investment 
				principles. We seek to time our exit from any of our investments 
				when it matures in its life cycle. We exit an investment when we 
				have completed our mission of taking the entity to the next 
				level and when we believe that the introduction of new 
				shareholders will benefit the entity; this is precisely what we 
				did with Sun Packaging last year and earlier with NAPCO and 
				Voltamp besides others. In Sun Packaging, we did not sell the 
				entire stake because even though we exited, we still believe in 
				the company. So for us, if we didn’t exit we could still be 
				satisfied with the investment.<br>
				<br>
				<b>Do you have any opportunities in the present circumstances by 
				which you can enhance shareholders’ value?</b><br>
				We are looking at a few opportunities and are evaluating them. 
				We have a stringent evaluation process and we participate in 
				opportunities where we believe we can add value – our belief is 
				in “Operational Enhancement” based investment. Our style of 
				investments is through partnerships – one of our core strengths. 
				We shall consider participation where we are certain that we can 
				add value and which pass our robust evaluation processes. We 
				have made it a priority to constantly seek and implement best 
				business practices and we constantly recalibrate ourselves to 
				optimise our position. The current macroeconomic factors remain 
				dynamic in the Sultanate and this will continue to provide Al 
				Anwar with opportunities.<br>
				<br>
				Please share with us some numbers like your company’s present 
				net worth and total investments <br>
				Al Anwar Holding’s consolidated net worth as on March 31, 2012 
				is RO18mn. The present estimated market value of its total 
				investments is RO24.5mn.<br>
				<br>
				<b>Does Al Anwar have any investments outside Oman?</b><br>
				We have three investments outside Oman and these are in Bahrain, 
				India and Kuwait. However our current focus shall be Oman and 
				UAE.<br>
				<br>
				<b>What is the pattern you follow in order to diversify your 
				stakes?</b><br>
				We make no attempt to diversify our investments. We maintain a 
				clear focus on the sectors we follow and companies in which we 
				participate. We are not a generalist investor but a focused and 
				hands-on investor. However we do practice several risk 
				management actions to protect our investments and capital. We 
				have a system of thorough and consistent tracking of the sectors 
				and evaluation of our investments along better risk management 
				practices. This is on account of considerable efforts of the 
				management of the different companies within our group that has 
				helped to shape our progress. <br>
				<br>
				<b>Family owned companies are the dominant market forces in 
				Oman. How do you view such an investment climate as compared to 
				other markets you have worked in?</b><br>
				There are several large family groups here in Oman and these 
				form a very valuable source of business expertise while also 
				being a source of capital. I am truly impressed with the level 
				of sophistication which some of them work with. The family 
				groups here are quite versatile and form a new class of 
				investors which play a huge role in the local economy. I have 
				worked in several markets around the world and each has its own 
				set of dynamics. Oman is different but has its charm of doing 
				international business with a sense of ethics and maturity. The 
				courtesy and politeness which is a part of the Omani culture, is 
				the foremost and the most obvious and powerful impression I 
				have. There is this richness of culture that has been preserved 
				even as there is great modernity all around. I have travelled 
				and lived in several countries and the culture that I find in 
				Oman is both unique and refreshing.<br>
				<br>
				Personal dignity and ethics are the cornerstones to businesses 
				here in Oman. This ensures that governance and fair play are 
				observed. The local businesses are becoming more sophisticated 
				and Oman is sure to make huge progress in the years to come.<br>
				<br>
				And the vision and wisdom of His Majesty Sultan Qaboos bin Said 
				will continue to move Oman forward and reach new heights.<br>
				<br>
				<b>Are there any areas of challenge in your associate or 
				subsidiary companies?</b><br>
				Our performance is related to the performance of each of our 
				individual investee companies. We do not walk away from 
				challenges but see them as new opportunities for growth and 
				efficiencies in our efforts to continue improving our earnings. 
				We are in a phase of a fast evolving market place. Our regional 
				investments have the local environmental and business dynamics. 
				It is also refreshing to note that the MOCI, CMA and the other 
				regulatory agencies have laid the formulation of good corporate 
				governance and transparency along with a robust legal system. 
				This makes the environment extremely business friendly.<br>
				<br>
				Each of our investments has their unique challenges which are 
				inherent to the business and the location in which they operate. 
				However we have a robust and thorough monitoring system and we 
				track the performance of each of our investee companies. We 
				actively participate in the boards and the sub committees. By 
				this participation we add the maximum value to our investments. 
				In our companies we strive to widen the gap with our competitors 
				by strengthening our technological and market leadership. We 
				hope to secure absolute competitiveness and also to survive any 
				market downturns by enhancing overall efficiency. We assist our 
				investee companies by participating in the build-ups of their 
				strategic long term blueprints for growth and direction. We are 
				very hands-on investor and look to use our experience in 
				building business more efficiently and economically.<br>
				<br>
				<b>Due to prevailing global market conditions, the capital 
				market in Oman is facing a major crunch. What is the way forward 
				for investing companies such as yours?</b><br>
				I believe that capital markets will constantly make adjustments. 
				However economies in Europe will be further tested and the 
				issues with borrowings and spending will need to be addressed. 
				The global economy is currently going through several changes. 
				All this will throw us challenges and this will come regularly. 
				I believe that once these challenges are met there will be fresh 
				ones which we will have to tackle. We have taken multiple 
				scenarios’ into consideration and have devised our investment 
				strategy. These are times of great opportunities and investment 
				companies with the right strategy can benefit from this. The 
				advantage we have is that we do not have a fund structure and 
				this enables us to readjust quickly to the changing dynamics. 
				The critical input for success in any business is the ability to 
				use the capital it employs more wisely, and this is more 
				pertinent to our industry.<br>
				<br>
				The past events have shown that we are a sector in transition. 
				We have seen several market stresses on similar business models. 
				The direction the industry is moving towards is apparent if we 
				look at some of the key trends. There is a greater emphasis on 
				prudence, and the irrational exuberance in investing in certain 
				highly volatile sectors, is declining. This is good and it shows 
				the maturity of several of the regional investment houses. 
				However this is not a voluntary turn of events but was a forced 
				outcome of the financial crisis. Perhaps now some key lessons 
				have been learnt and we hope that this message is not quickly 
				forgotten when markets open up again.<br>
				<br>
				While the world is changing, our values shall not change and we 
				are guided by our principles. We continue to manage our business 
				around the evolving needs of the market place, so that we can be 
				at the vanguard of change. To accomplish our goals we have 
				committed ourselves to our principles on what we do and how we 
				do it. 2011 was an interesting year with a challenging economic 
				environment along with the consumption slowdown in developed 
				countries as a fall out of the European crisis.&#8194;<br>
