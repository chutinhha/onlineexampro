						<font face="Verdana" size="4" color="#C00000">
						Omanis keen to bridge gap between education and job 
						requirements: Survey</font></b><p align="justify" style="margin: 3px 10px">
						<b>
						<font face="Verdana" size="2">
                		<br>
						Preliminary results of largest workplace study, 
						QuduratTM, announced by Aon Hewitt says that despite 
						ongoing efforts, Omanis continue to report lower levels 
						of work engagement. Nationals report lower levels of 
						satisfaction with their education to support their work 
						preparedness</font></b><p align="justify" style="margin: 3px 10px">
						&nbsp;<p align="center" style="margin: 3px 10px">
						<img border="0" src="images/inthenews-sep-12-1.jpg"><p align="justify" style="margin: 3px 10px">
				<font face="Verdana" size="1">
				<br>
				Omani nationals are keen to bridge the gap between their 
				educational preparation and their actual job requirements, 
				according to the preliminary findings of the ground-breaking 
				QuduratTM research, announced by Aon Hewitt, the global human 
				resources business of Aon.<br>
				QuduratTM is the largest workplace study in the region, focused 
				on the advancement of national talent, the single biggest HR 
				issue in the region. QuduratTM – meaning ‘capabilities’ in 
				Arabic – is unprecedented in scope and scale, voicing the 
				opinion of more than 20,000 employees across seven countries 
				(UAE, Qatar, Kuwait, Saudi Arabia, Oman, Bahrain and Egypt). 
				With final results expected to be announced region-wide by 
				December 2012, insights from the study are expected to 
				significantly impact HR practices and human capital development 
				in the region. <br>
				<br>
				The study was first launched in 2011, and produced many 
				counter-intuitive findings which surprised and challenged the 
				preconceptions employers had in the region about local talent 
				and nationals in the workforce. Among the most unexpected 
				findings were that Omani nationals reported lower levels of 
				engagement in the public sectors than they did in the private 
				sector, and that less than half had confidence in the most 
				senior leadership in their respective organisations.<br>
				<br>
				<b>Growing disconnect<br>
				</b>The preliminary results of this year’s report reveal that 
				Omanis are reporting a growing disconnect between their job 
				requirements and the preparation they have received for those 
				requirements through their education, compared to foreign 
				workers. In addition, Omani nationals continue to report lower 
				levels of work engagement than expatriates, which could impact 
				their longer term motivation, performance and productivity at 
				the workplace. The report shows similar results for nationals 
				across all seven countries involved in the study.<br>
				<br>
				Commenting on these initial findings, Radhika Punshi, Aon 
				Hewitt’s head of applied research (MENA) says, “As Oman’s and 
				the region’s economy continues to recover, there will be a 
				renewed and exponential growth focus on attracting, motivating 
				and retaining top talent within all industrial sectors, with a 
				greater emphasis on quality. However, more than ever before, 
				there is a greater urgency for alignment and collaboration 
				between the education system and the demand from employers.”<br>
				<br>
				“Our early findings point to a pressing mismatch between 
				education and employment, significantly impacting the 
				motivation, expectations and preparedness of current and future 
				employees towards their jobs. On a more positive note, the fact 
				that there is an awareness among Omani nationals of this 
				mismatch points towards a willingness to address this problem,” 
				she added.<br>
				<br>
				<b>100 participating organisations<br>
				</b>This year’s QuduratTM study already has over 15,000 
				responses from approximately 100 participating organisations in 
				UAE, Qatar, Saudi Arabia, Bahrain, Kuwait, Oman and Egypt. The 
				first wave of the study, concluded last year had over 4,600 
				respondents in total.<br>
				<br>
				The study has also received strategic support through 
				partnerships with a host of governmental organisations across 
				the participating countries, including Abu Dhabi Tawteen 
				Council, which spearheads the efforts of the Abu Dhabi 
				government in sustainable Emirati workforce development. Other 
				organisations which supported the study include TECOM 
				Investments in Dubai, a diversified conglomerate with strategic 
				investments in sectors that contribute to the development of 
				Dubai’s knowledge-based economy; Tamkeen Bahrain, which is 
				tasked with supporting Bahrain’s private sector and positioning 
				it as the key driver of economic development and Bahrain’s 
				Institute of Public Administration, which aims to enhance the 
				skills and competencies of government employees in the Kingdom.&#8194;</font></td>
