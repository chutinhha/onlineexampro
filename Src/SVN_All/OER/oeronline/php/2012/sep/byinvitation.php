                  <p align="justify" style="margin: 3px 10px"> 
					<b><font face="Verdana" size="1">&nbsp;<br>
					</font><font face="Verdana" size="4" color="#C00000">
					Data Leak – a lurking threat</font></b></p>
                </td>
		</tr>
	<tr>
		        <td valign="top" width="100%"> 
				<p align="justify" style="margin: 10px; "> 
				<font face="Verdana" size="1">
				<b>It is essential for everyone to understand the importance of 
				Data for an organisation and to take ownership for its health 
				and safety<br>
				<br>
				By Manjish Deb</b></font></p>
				<p align="justify" style="margin: 10px; "> 
				<font face="Verdana" size="1">
				<b>
				<br>
				</b>Carol Wood, a student of University of Virginia was surfing 
				the internet when she suddenly found her social security number 
				(SSN) along with several other confidential data online. She 
				checked and there were similar confidential data available of 
				around 350 more students including their exam transcripts, SSN, 
				credit screening information etc.<br>
				<br>
				Around 150 million customers’ data were stolen and made 
				available online from Shanghai Roadway D&amp;B Marketing Services. 
				More than 465,000 records including driver’s license, social 
				security numbers etc were stolen by a government employee from 
				Georgia Technology Authority. Similarly, around 1,85,000 records 
				were stolen from San Jose Medical Group including SSN, credit 
				card information etc. The list is long and growing at an amazing 
				pace. And all these cases involve someone from inside leaking 
				information, intentionally or unintentionally. <br>
&nbsp;</font></p>
				<p align="center" style="margin: 10px; "> 
				<img border="0" src="images/byinvitation-sep-12-1.jpg" width="300" height="215"></p>
				<p align="justify" style="margin: 10px; "> 
				<font face="Verdana" size="1">
				<br>
				In a study done by an independent institute which identified 43 
				companies who had suffered data leaks last year, the total cost 
				of coping with the consequences rose to $6.6 mn per leak, with 
				84 per cent repeat offenders. Especially for the healthcare and 
				financial services sector, the customer discontinued the 
				association with companies that failed to secure sensitive data.<br>
				<br>
				Let’s analyse how and why a data leakage occurs. A few other 
				ways of losing the data via leaks are:<br>
				Lax, improper or missing access control systems containing 
				sensitive data, from back-end databases and servers to mobile 
				computers<br>
				<br>
				Insecure transmission of personal identifiable and other 
				restricted data<br>
				Insecure and improper destruction of information, encompassing 
				physical locations (dumpsters) and electronic media (laptops and 
				backups)<br>
				<br>
				Lack of separation of duties and access controls on databases 
				and other shared systems<br>
				Doing business with providers that allow data leakage (HR 
				sending sensitive information to do background check)<br>
				<br>
				<b>How to prevent Data Leaks: </b><br>
				Some important questions you should ask your IT staff or IT 
				provider are:<br>
				Are there any policies and procedures in place to ensure that 
				the organisations data is not “leaking” outside of the network?<br>
				<br>
				Is there any technology in place to monitor these policies?<br>
				<br>
				Is there any way of preventing employees from taking corporate 
				data or sending it to unauthorised parties? <br>
				<br>
				Is our ‘Data Loss Prevention’ policy meeting any compliance 
				requirements we might have (HIPAA, ISMS, GLBA, SOX, etc).<br>
				<br>
				Are we aware of where our most confidential data is being held?
				<br>
				<br>
				Are we able to audit users’ access to our confidential data?<br>
				<br>
				Primary concern for Data Loss Prevention is to understand what 
				the value of the data is to the organisation and how everyone is 
				responsible for ensuring its ‘health and safety’. In essence, 
				this can only be achieved through a comprehensive understanding 
				of the risk facing your organisation’s data, and educating the 
				people who handle or access it. Training and educating should be 
				mandatory for all people who come into contact with it and this 
				is only the first step. This may appear like a basic concept, 
				but it is one that is often neglected. This is the primary 
				reason for data breaches.<br>
				<br>
				Data is any company’s most valuable asset and ensuring that it 
				is protected against ‘internal’ threats is just as important as 
				securing it against outside threats.<br>
				<br>
				For most companies, data loss is largely attributed to 
				employees. According to a recent Gartner report: <br>
				One in every 400 messages contains confidential data<br>
				One in every 50 network files is wrongly exposed<br>
				Four out of five companies have lost data on laptops<br>
				<br>
				Half of all companies have lost data on USB drives<br>
				<br>
				More than 52 per cent of CIOs (Chief Information Officers) 
				believe data leakage is a top priority in their security 
				spending<br>
				<br>
				A common proverb says: Prevention is better than Cure. It’s 
				important not to wait until a breach occurs to implement data 
				leakage solutions. Without a comprehensive security structure of 
				your network, you may not even know if a security breach occurs.<br>
				<br>
				A Data Leak Prevention system will help you to plug-in the loop 
				holes that you may have in your system. A DLP solution will not 
				only alert you of any breaches, but prevent any devious 
				activities of all the users so that you are aware of activities 
				and take preventive measures.<br>
				A few things to look for in a Data Loss Prevention solution are:<br>
				<br>
				Where does the product look for data across your network?<br>
				<br>
				Does it find sensitive data just travelling your network, on 
				your database and file servers, or does it look at data on local 
				desktops?<br>
				<br>
				Can the product search for data without any endpoint agents 
				installed, or does it require a client to be installed on the 
				end-points?<br>
				<br>
				Can the Data Loss Prevention agents accomplish other 
				security-related things on the endpoints?<br>
				<br>
				Some vendors can turn off USB connectors to block someone with a 
				thumb drive from walking away with all of your customer data in 
				their pocket. Others can control which applications can and 
				can’t be run on your workstations, laptops or even tablets.<br>
				<br>
				What protocols can be blocked or analysed?<br>
				<br>
				Just protocols involving e-mail (SMTP, POP and IMAP)? What about 
				file transfer technologies or instant messaging?<br>
				<br>
				What kind of data can be saved by the users on their end-points?<br>
				<br>
				Can they save information like credit card information, SSN, 
				Telephone numbers, etc on their desktops?<br>
				<br>
				How hard is it to create – and then change – the Data Loss 
				Prevention rules?<br>
				<br>
				A DLP tool is only as good as its ability to have rules updated 
				easily over time. Can your IT staff (or outsourced provider) 
				easily update rules as new threats are identified or company 
				policies updated?<br>
				<br>
				What happens when a rule is broken?<br>
				<br>
				Can you figure out who violated the policy, where the offending 
				information is stored, and what kinds of automated responses can 
				be sent? Does the product come with pre-defined templates to 
				make all of this easier?<br>
				<br>
				Is the content analysis portioning a separate or integrated 
				piece of the product?<br>
				<br>
				In some cases, you need several different products to be 
				installed to enable a complete solution.<br>
				<br>
				What kinds of reports are available, and are they easy to 
				understand?<br>
				<br>
				Does the product offer any real-time reporting capabilities, and 
				how flexible are these reports?<br>
				<br>
				<b>How to achieve a successful DLP implementation:</b><br>
				Identify key participants – Assemble those that should be 
				involved internally when you identify data loss. Participants 
				may include IT, HR, and operations employees. Identify the 
				individuals and meet with them to work out what situations they 
				will need to be involved in.<br>
				<br>
				Develop a notification process – Do you have processes ready if 
				a regulated data breach occurs? Who will be notified? Is your 
				legal or compliance team ready to meet requirements such as 
				breach notification laws?<br>
				<br>
				Fix broken business and weak processes – Assume that you will 
				find broken business processes, like automated file transfers to 
				partners in clear text over the internet instead of encrypted or 
				over private line.<br>
				<br>
				Create a plan for handling theft – Talk with HR to establish a 
				process if you uncover insider theft. Give HR a heads up and 
				involve them in the roll-out. The insider may be at a senior 
				level, so consider that as well.<br>
				<br>
				Establish the response team and workflow – Map out your incident 
				handling and resolution process, as a flowchart. Who will be on 
				the incident handling team?<br>
				Set a timeline for incident resolution – Set goals for making 
				sure incidents are handled in a timely manner:<br>
				First level review of all incidents within X amount of time<br>
				Resolve all high severity incidents within Y amount of time<br>
				Close all incidents within Z amount of time (resolving incidents 
				within two hours)<br>
				Establish reporting and automate – How are you going to track 
				things? Decide what reports you’ll need to have and who should 
				get them. There should be reports for:<br>
				Incidents created<br>
				Incidents closed<br>
				Open incidents status – by age, severity, owner<br>
				A report sorted by the type of data or by policy that was 
				violated<br>
				Summary reports for your CSO<br>
				Plan roll-out stages – It’s important to plan your roll-out in 
				stages rather than trying to attach the problem all at once.<br>
				<br>
				Select data and policies to be implemented in stages<br>
				Roll-out and test your policies in a monitor only mode, to set a 
				baseline.<br>
				Decide when you will have the solution, notify end users and 
				what you expect of them.<br>
				<br>
				<b>How can DLP help you? <br>
				</b>A DLP system primarily helps enforce ‘acceptable use’ 
				policies and processes for an enterprise. They are not designed 
				to solve the part of data leakage problem space that is related 
				to security threats like a virus/trojan attack; it will not stop 
				hackers coming into your system; it will not stop phishing 
				attempts on your corporate e-mail system. So, it is not an 
				information security data leakage issue that the DLP solution is 
				trying to solve.<br>
				Hence the DLP solutions help mitigate following risks:<br>
				Identifying insecure business processes. For example, use of FTP 
				for transporting PHI data<br>
				Accidental data disclosure by employees. For example, employee 
				sending unencrypted email containing PHI data<br>
				Intentional data leakage by employees. For example, disgruntled 
				employees stealing data or an employee leaving the company with 
				sensitive data<br>
				Stopping users storing and distributing sensitive information in 
				their desktops or mobile devices.</font></p>
