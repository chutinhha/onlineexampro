                		&nbsp;</font><p align="justify" style="margin: 3px 10px">
						<font face="Verdana" size="1">
						The residential cum commercial complex of Hassan Juma 
						Backer (HJB) has been built with the intention to 
						provide a superior work place and better quality of 
						residences for the company’s workforce. Each and every 
						feature of the building is relevant to green concepts. A 
						spacious and healthy work place has enhanced efficiency 
						of the workforce. Visitors to this office complex have 
						admired the features and are sure to take away ideas for 
						their own projects. The main contribution to Oman is 
						energy savings, reduction in waste, time savings and 
						ultimately making a healthy community.<br>
						<br>
						HJB has adopted modern technologies to build an 
						eco-friendly building complex by identifying green 
						concepts and design features and incorporated them 
						keeping in mind the natural habitat, light pollution, 
						energy conservation.<br>
						<br>
						The office has adopted full online documentation system. 
						The concept of electronically signing the contracts is 
						also on the anvil. This way paper usage has been reduced 
						drastically. The external walls of the main building 
						have been constructed with composite insulated material 
						which dissipates the sun’s heat thereby maintaining the 
						ambient temperature inside the room for energy savings. 
						Working in an environment with poor air quality can lead 
						to harmful side effects that range from eye and throat 
						irritation to long-term effects like respiratory disease 
						and cancer.<br>
						<br>
						The open atrium gives natural circulation of air and 
						reduces carbon dioxide (CO2) levels. The air 
						conditioning system is VRV type which provides positive 
						fresh air pressure in every nook and cranny of the 
						office area. All this has helped in creating a healthy 
						breathing atmosphere inside the building complex.<br>
						<br>
						<b>Naturally happy </b><br>
						It has been found out that having natural day lighting 
						is always the greenest way to go ahead. Plus, there’s 
						also a growing body of evidence to suggest that workers 
						in buildings with high levels of natural light are 
						happier and more productive than those more reliant on 
						artificial light. Provision for natural lighting 
						facility wherever possible has been made thus saving 
						power energy.<br>
						<br>
						The company has been able to minimise the day-to-day 
						utilisation of resources like water and paper thereby 
						protecting the environment from pollution, preventing 
						ozone depletion and saving trees. Another interesting 
						initiative has been to use eco-friendly fonts 
						(preferably Garamond) for optimisation of printing ink; 
						reducing the print area margins, selecting the correct 
						size of the paper, previewing before printing and alert 
						message before printing preventerroneous prints thereby 
						saving paper, ink and energy.<br>
						<br>
						Most of the staff members have been provided with 
						accommodation in the same premises where the office is 
						located while others have been giving accommodation in 
						the nearby vicinity thereby reducing the load on 
						commuting system. The sewage and waste water of the 
						building is collected in underground water retaining 
						structure (holding tank) and sent to the Sewage 
						Treatment Plant for recycling. No soak pits are in use 
						thereby preventing any subsoil pollution. Level 
						monitoring instrument is installed in the holding tank 
						by which the scheduling of the dewatering agency is 
						ensured and spillage is avoided.&#8194;</font></td>
