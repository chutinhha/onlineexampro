						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">
						Telecommunications main driver of economic development</font></b><p align="justify" style="margin: 3px 10px">
						<b>
						<font face="Verdana" size="2">
                		<br>
						</font></b><b>
						<font face="Verdana" size="2">
                		The TRA Annual Report 2011 focuses on the continued 
						progress made by the telecommunications sector of Oman 
						after the introduction of the liberalisation policy in 
						2003</font></b><p align="justify" style="margin: 3px 10px">
						&nbsp;<p align="justify" style="margin: 3px 10px">
				<font face="Verdana" size="1">
				<br>
				</font>
						<img border="0" src="images/inthenews-nov-12-2.jpg" width="200" height="299" align="right" hspace="7" vspace="3"><font face="Verdana" size="1">In 
				the last decade, the telecommunications sector has made 
				remarkable achievements which helped in the reduction of prices 
				of telecom services, improvements in the quality of service and 
				expansion of networks in the Sultanate of Oman.<br>
				<br>
				All credit for this goes to the Telecommunications Regulatory 
				Authority (TRA) of Oman which was established in 2002, to 
				liberalise and promote the telecommunications services in the 
				Sultanate under the Telecommunications Act which was issued 
				under the Royal Decree No. 30/2002.<br>
				<br>
				Highlighting the developments in the telecommunications sector, 
				the TRA Annual Report 2011 talks about the growth of the sector 
				by dissecting the performance of the individual service 
				providers vis-à-vis the products and services launched by them. 
				The report also provides an overview on how the companies have 
				been faring in the face of stiff market competition which 
				includes two legacy operators and four mobile resellers.<br>
				<br>
				In the mobile telephony segment, the number of mobile phone 
				subscribers has increased significantly over a period of five 
				years registering a compound annual growth rate (CAGR) of 17.8 
				per cent since 2007 but the fixed line telephony witnessed 
				comparatively a sluggish growth.<br>
				<br>
				The total telecom sector revenue, from operators and re-sellers 
				reached an all time high of RO694mn during the financial year 
				2011. This represents an increase of 9.3 per cent compared to 
				RO634mn in the previous year.<br>
				<br>
				The contribution of revenue from mobile services stands at 69.4 
				per cent of the total telecom revenue. The remaining 30.6 per 
				cent of the total telecom revenue is from fixed line, internet 
				and other services.<br>
				<br>
				<b>Broadband decade<br>
				</b>In a statement in the annual report, HE Dr Mohammed bin 
				Hamad Al-Rumhi, chairman, TRA says, “Drawing on fact that in 
				this millennium, telecommunications is one of the main drivers 
				of economic development, the TRA has made significant efforts to 
				establish a modern regulatory framework to develop the 
				telecommunications sector in Oman despite numerous challenges. I 
				take this opportunity to express my profound gratitude to the 
				previous board, which have left a very good legacy and set an 
				excellent base for the new board to advance the work further and 
				capitalise on the achievements.”<br>
				<br>
				According HE Rumhi, the TRA, however, still has a big challenge 
				ahead to meet the public demand of introducing latest 
				technologies in telecom services, expanding telecom networks 
				throughout the Sultanate, connecting 100 per cent population and 
				regulating the services to achieve best value of money the 
				citizens pay for telecom services. The next decade belongs to 
				broadband, smart devices and data communications and the TRA is 
				ready to address the issues and emerging challenges.<br>
				<br>
				The new CEO of TRA, Dr Hamad bin Salem Al-Rawahi, who assumed 
				charge in the middle of 2011, says that during the second half 
				of 2011, the TRA mainly pursued the ongoing tasks initiated by 
				the previous board. “I am confident that the TRA will meet the 
				expectations of the people of Oman to bring about all what is 
				needed for the development of the country and well being of the 
				citizens under the wise guidance of the board and we will honour 
				the confidence and trust reposed in us,” he says.</font></td>
