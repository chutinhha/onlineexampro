						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">
						Incredible India beckons Omanis</font></b><p align="justify" style="margin: 3px 10px">
						<b>
						<font face="Verdana" size="2">
                		<br>
						India’s ministry of tourism is eyeing the Omani market 
						thanks to the growing number of visitors from the 
						Sultanate both for business and pleasure</font></b><p align="justify" style="margin: 3px 10px">
						&nbsp;<p align="center" style="margin: 3px 10px">
						<img border="0" src="images/inthenews-nov-12-1.jpg" width="400" height="266"><p align="justify" style="margin: 3px 10px">
				<font face="Verdana" size="1">
				<br>
				India has been witnessing a steady rise in the number of Omani 
				tourists and is making all out efforts to lure them away from 
				the traditional areas of attraction. A large number of Omanis 
				continue to visit Germany for medical treatment while the Far 
				East usually holds sway as a leisure destination. But that is 
				changing now as the Indian Ministry of Tourism woos Omanis by 
				promoting India’s myriad attractions aggressively.<br>
				<br>
				As a recognition of Oman’s contribution to India’s tourism 
				revenues, the Ministry of Tourism is planning to open a tourism 
				representative office here in Oman. This was revealed at the 
				‘Incredible India’ tourism road show organised by India’s 
				Ministry of Tourism in association with the Embassy of India, 
				Oman.<br>
				<br>
				A high-level delegation comprising Girish Shankar, additional 
				secretary at Ministry of Tourism, government of India, V Vasanth 
				Kumar, Minister of Tourism and Culture, Archaeology and Museums, 
				Government of Andhra Pradesh, and Vikas Rustagi, regional 
				director, India Tourism (Dubai) were part of the delegation that 
				gave a a glimpse of “the attractive tourism products as well as 
				the countless cultural, spiritual and travel experiences that 
				India offers as a tourist destination”.<br>
				<br>
				HE J S Mukul, Ambassador of India, said that the number of Omani 
				visitors to India grew by 20 per cent reaching 42,208 in 2011 
				and was 39,781 in <br>
				the first nine months (till September) in 2012.<br>
				<br>
				In his presentation, Shankar said that foreign tourist arrivals 
				to India had witnessed a steady increase over the years. 
				“India’s travel and tourism industry is ranked fifth in the 
				world,” he pointed out.<br>
				<br>
				<b>Development budget accomodation<br>
				</b>“India is promoting tourism for economic development and to 
				promote effective dialogue. We are keen on developing budget 
				accommodation,” he added.<br>
				<br>
				According to Shankar, India is looking forward to building a 
				strong relationship among stakeholders and wants to have 
				frequent interactions with partners overseas, while adding that 
				the government has invited foreign direct investment (FDI) in 
				the hotel industry also. Medical tourism in India is growing at 
				a healthy rate of 20 per cent per annum. Shankar adds that Oman 
				has been one of the major trading partners of India and they are 
				keen on taking that relationship to the next level in all 
				sectors.<br>
				<br>
				In his speech, Vasanth Kumar said, “tourism has an immense 
				potential in rural areas of India and is proving to be a major 
				source of employment.” He said that Andhra Pradesh has excellent 
				MICE capabilities and can host major conventions for upto 10,000 
				people at one time. He informed that the state of Andhra Pradesh 
				has many places of tourism interest and invited visitors to 
				explore the land.<br>
				<br>
				In 2011, India had 740 million domestic travellers across 
				segments. In the same year, total Foreign Tourist Arrivals 
				(FTAs) in India were 6.29 million with foreign exchange earnings 
				of $16.69bn (global rank-17) up 17.6 per cent from 2010.<br>
				<br>
				India’s foreign exchange earnings from tourism grew by an 
				impressive 14.1 per cent compound annual growth rate (CAGR) 
				during 2011-2012, nearly double the global average.&#8194;</font></td>
