						<p align="justify" style="margin: 3px 10px"> 
					<b><font face="Verdana" size="4" color="#C00000">
					Bright prospects for partnerships</font></b><p align="justify" style="margin: 3px 10px"> 
					<font face="Verdana" size="2"><b><br>
						</b></font><b><font face="Verdana" size="2">HE Alex 
					Chernov AC QC, Governor of Victoria in Australia speaks 
					about the potential areas of cooperation between Victoria 
					and Oman in an exclusive interview with Visvas Paul D Karra</font></b><p align="justify" class="style13">
				&nbsp;<p align="center" class="style13">
				<font face="Verdana" size="1">
				<img border="0" src="images/passingby-nov-12-1.jpg" style="border-style: solid; border-color: #808080; " width="400" height="301"></font><p align="justify" class="style13">
				&nbsp;<p align="justify" class="style13">
				Academician, legislator and head of a state, HE Alex Chernov, 
				governor of Victoria, Australia, has witnessed an entire gamut 
				in the lifecycle of a country. Therefore his experience speaks, 
				when he says, “I am here to establish closer ties and emphasise 
				the relationship Victoria has with Oman and the only way to 
				emphasise this is by a personal visit and by meeting people.”<br>
				Says HE Chernov, “Cooperation between any two countries or 
				states is very critical nowadays. Because almost all countries 
				face the same set of problems ultimately. In Victoria, our 
				state, we have had our own spells of droughts and floods and 
				bush fires which are no less challenging than the situations 
				that one may come across in countries like Oman where you have 
				issues of soil conservation, salinity, ground water availability 
				etc.”<br>
				<br>
				Stressing on the importance of having bilateral cooperation and 
				joint ventures for economic development, HE Chernov quotes 
				Indian education minister, Kabil Sibal, who said: “Today, the 
				problems are too big for any country to solve it by themselves. 
				Therefore you need to have joint ventures and partnerships.”<br>
				<br>
				HE Chernov had a meeting with HE Dr Rawiyah Bint Saud Al 
				Busaidiyah, Oman’s Minister of Higher Education where the two 
				discussed cooperation between Australia and the Sultanate in 
				higher education. They also discussed about the Sultanate’s 
				establishment of the Sultan Qaboos Chair for Arab and Islamic 
				Studies at the University of Melbourne in Australia, which has 
				got the 32nd place in the list of leading universities, and the 
				setting up an Omani corner at the university hosting some Omani 
				heritage and culture products.<br>
				<br>
				“The Omani corner at the university has been a great success so 
				far because it gives you a glimpse of the Sultanate of Oman. I 
				realise that your country is so beautiful,” says HE Chernov.<br>
				<br>
				<b>Expertise for projects<br>
				</b>During the meeting with HE Rawiyah, a number of joint 
				venture proposals were discussed by HE Chernov. These include 
				conducting joint scientific research between the research 
				institutions and universities in the two countries in 
				quantitative survey of palm trees diseases and providing more 
				chairs for the Omani students at other Australian universities. 
				Other areas of mutual cooperation such as providing expertise 
				for vital projects like the Oman University and Science and 
				Technology City were also talked about.<br>
				<br>
				HE Chernov also met HE Dr Yahya Bin Mahfoudh Al Mantheri, 
				chairman of the State Council, where they spoke about bilateral 
				relations particularly related to the parliamentary aspects and 
				ways to enhance them.<br>
				<br>
				HE Mantheri highlighted the progress of the Shura while talking 
				about the importance of the legislative and oversight powers of 
				the Council of Oman and the impact of these powers on activating 
				a parliamentary process.<br>
				<br>
				HE Chernov was born in Lithuania and, with his Russian parents, 
				immigrated to Australia as a young boy. He was educated at 
				Melbourne High School and then at the University of Melbourne 
				where he gained the degrees of B.Com. and LLB (Hons). In 1968, 
				Alex Chernov signed the Roll of Counsel at the Victorian Bar and 
				in 1980, was appointed Queens Counsel in Victoria. He played a 
				significant role in providing leadership to the legal profession 
				and legal education in Australia.<br>
				<br>
				In 1997, HE Chernov was appointed as a Judge of the Trial 
				Division of the Supreme Court of Victoria and, in 1998, he was 
				appointed to its Court of Appeal. After various roles in the 
				legal field and as academician, he was elected as Melbourne 
				University’s chancellor in 2009. Earlier in 2008, he was 
				appointed as an Officer of the Order of Australia and was 
				awarded a Companion of the Order of Australia at the 2012 
				Australia Day Honours. He was sworn in as the 28th governor of 
				Victoria in April 2011.<br>
				<br>
				Talking about his earliest association with Oman, HE Chernov 
				says it goes back 2003, when His Majesty the Sultan endowed a 
				chair in Arabic and Islamic studies in Melbourne University. 
				They managed to get Prof Abdul Said into the chair who has done 
				a remarkably wonderful job. And even as the number of Muslims 
				continued to grow in Australia in the last 10 years, the Sultan 
				Qaboos chair became a focal point in the Asia Centre of the 
				university. Two years ago, the Oman corner was opened and the 
				Counsel General of Oman, Mahmoud Al Hajiry opened a mini museum 
				there. Anybody passing there gets a glimpse of Oman, says HE 
				Chernov.<br>
				<br>
				<b>Education, education, education<br>
				</b>Melbourne city has an international appeal with just under 
				half of the population of the city having been born overseas or 
				having parents who were born overseas. Melbourne and the other 
				universities in Victoria have about a quarter of their students 
				who are international from the region.<br>
				<br>
				HE Chernov says that higher education is accorded top priority 
				in Melbourne and therefore it is home to some of the top 
				universities in Australia. Emphasising this, he says that in any 
				developing country, the most important thing is education, the 
				second most important thing is education and the third most 
				important thing is education. And he is happy that Oman has also 
				given the same importance to education. To honour HE Chernov’s 
				visit to the Middle East, an exclusive Victorian alumni 
				networking event was hosted at the Grand Hyatt by John Butler, 
				the commissioner for Victoria to the Middle East and North 
				Africa. Alumni from all universities in Victoria were invited to 
				this event and HE Chernov was pleased with the way the people 
				recollected their memories of Australia. He says it was 
				interesting to find that the Omani alumni were fond of 
				reminiscing about their memories of Melbourne and they even made 
				an audiovisual presentation of their days in Melbourne.<br>
				<br>
				<b>Joint research<br>
				</b>According to HE Chernov, there are many facets of education. 
				And one of these is vocational education. He says that Victoria 
				and Oman could work together because the type of colleges they 
				have in Melbourne and Victoria are pretty much renowned and top 
				of the range in teaching skills to even plumbers and 
				electricians who are involved in infrastructure development. 
				Moreover, these vocational students if given an opportunity 
				could go on and study for their degree certificate. It’s a two 
				step process. So they learn both theory and practice.<br>
				<br>
				Apart from education, HE Chernov feels that Oman and Victoria 
				could have collaboration in a number of areas. Some of these 
				include infrastructure development, heritage and culture, 
				life-sciences and biotechnology, manufacturing (especially 
				automobiles) and tourism.<br>
				<br>
				“Victoria, although a small state, contributes 25 per cent of 
				the Gross National Product and our agricultural exports is huge 
				almost 30 per cent out of which 90 per cent are dairy products,” 
				he points out.<br>
				<br>
				Another great strength of Victoria is life sciences and 
				biotechnology. Many universities are involved in research but 
				most of them need capital infusion to continue their research. 
				It would be great if Oman could become a joint venture partner, 
				HE Chernov avers.</td>
