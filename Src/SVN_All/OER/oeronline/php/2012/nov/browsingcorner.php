				<img border="0" src="images/browsingcorner-nov-12-1.jpg" align="right" style="border: 1px solid #C0C0C0" hspace="12" vspace="3" width="140" height="199"><b>Ruchir 
				Sharma provides valuable insights on spotting the next economic 
				success in his captivating book on emerging economies. An OER 
				review</b><br>
				<br>
				The old rule of forecasting was to make as many forecasts as 
				possible and publicise the ones you got right. The new rule is 
				to forecast so far in the future, that no one will know you got 
				it wrong.’ Ruchir Sharma does neither. In Breakout Nations he 
				shows why the economic ‘mania’ of the 21st century, with its 
				unshakeable faith in the power of emerging markets - especially 
				China - to continue growing at the astoundingly rapid and 
				uniform pace of the last decade, is wrong. The next economic 
				success stories will not be where we think they are.<br>
				<br>
				<b>Snakes and ladders<br>
				</b>In this provocative book, Sharma analyses why the basic laws 
				of economic gravity (such as the law of large numbers, which 
				says that the richer you are the harder it is to grow your 
				wealth at a rapid pace) are already pulling China, Russia, 
				Brazil and other vast emerging markets back to earth. There are 
				a series of snapshots about the other important emerging 
				economies, in which he makes a series of sensible judgements.
				<br>
				<br>
				Sharma lavishes praise on South Korea, the ‘Germany of Asia’, in 
				particular for the way it has become a manufacturing powerhouse 
				in one generation and managed to turn adversity to advantage. In 
				the Asian financial crisis of 1998, it had to go to the IMF for 
				a bail-out loan. But it used this crisis to reorganise its 
				industry, allowing many weaker companies to go under or be taken 
				over, and by the middle of 2001 had repaid the debt. Sharma 
				believes South Korea will manage a successful unification with 
				the North. He is intrigued by Africa, in particular by South 
				Africa and Nigeria; by Turkey; Indonesia and by emerging Eastern 
				Europe. These are snapshots, but thoughtful ones that enable one 
				to calibrate one’s own views and expectations.<br>
				<br>
				To understand which nations will thrive and which will falter in 
				a world reshaped by slower growth, it is time to start looking 
				at the emerging markets as individual cases. Sharma argues that 
				we must abandon our current obsession with global macro trends 
				and the fad for all-embracing theories. He offers instead a more 
				discerning, nuanced view, identifying specific factors - 
				economic, political, social - which will make for slow or fast 
				growth.<br>
				<br>
				Spending much of his professional life travelling in these 
				countries as head of Emerging Markets at Morgan Stanley, Sharma 
				is uniquely placed to present a first-hand insider’s account of 
				these new markets and the changes they are undergoing. As the 
				years of unbelievably swift growth draw to their close, this 
				book shows us how it is time for both investors and economists 
				to halt their blind thrust towards an impossible future.<br>
				<br>
				Overall, the book stresses the fact that there is no magic wand 
				that creates sustainable growth, and we should recognise that 
				much of what is happening is catch-up. Sharma wisely notes: “All 
				the hottest new things, from tablet PCs to cloud computing to 
				social networking, are emanating largely from the United 
				States.” But this is a great road-map to the new and 
				better-balanced world in which we will all live, and an 
				encouraging one.</font></p>
