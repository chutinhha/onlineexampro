						<strong><span class="style27">The Spirit of Success</span><span class="style14"><br>
&nbsp;</span></strong><p align="justify" class="style13">
						<b>The Al Mar’a Excellence Awards 2012 spelt 
						‘achievement’ and ‘excellence’ at every level…</b><br>
						<br>
						&nbsp;<p align="center" class="style13">
						<img border="0" src="images/event-nov-12-1.jpg" width="500" height="333"><p align="justify" class="style13">
						<br>
						The second edition of the Al Mar’a Excellence Awards was 
						held at a dazzling ceremony on the lawns of Crowne Plaza 
						Hotel on October 7, 2012. The Awards recognised and 
						felicitated women achievers from diverse fields. HE 
						Maitha al Mahrouqi, Undersecretary, Ministry of Tourism 
						and HH Sayyida Rawan Al-Said, CEO, ONIC Holding graced 
						the event as the guests of Honour. <br>
						<br>
						Corporate high-fliers, the first Omani female 3G 
						engineer, a popular social media expert, adventurous 
						sailors, enterprising businesswomen , talented artists, 
						game changers – the Al Mar’a Excellence Awards 2012 
						spelt ‘achievement’ and ‘excellence’ at every level. <br>
						<br>
						Aisha Al Kharusi won the coveted Woman of the Year Award 
						while Salma Al Hajri was adjudged as the Most Promising 
						Woman of the Year. Apart from these the Al Mar’a 
						Excellence Awards felicitated women from diverse 
						categories like - Corporate Leadership; Fashion Design; 
						Fine Arts; Educational Services; Entrepreneurship and 
						Innovation; Health Services; Science and Industry; 
						Technology; Sports; Petroleum Services and Social 
						Responsibility. Al Mar’a also recognised three women 
						with Special Awards. The winners were adjudged on a host 
						of parameters like innovation and creativity, impact of 
						their contribution on society, leadership abilities and 
						goals achieved.<br>
						<br>
						The event was held in cooperation with the Ministry of 
						Social Development. The event had on board: Official 
						Auditing Partners: Ernst &amp; Young. Strategic Partner: 
						Audi. Support Partners: ONIC Holding (National Life &amp; 
						General and Oman Orix Leasing Company SAOG), Jotun 
						Paints, Jawahir Oman, Home Centre, Areej Vegetable Oils 
						and Muscat Pharmacy (Givenchy). Media Partners: Times of 
						Oman &amp; Al Shabiba.<br>
						<br>
						HH Sayyida Rawan set the stage for the event with her 
						opening speech in which she applauded the growing 
						profile of Omani women in the Sultanate. Rekha Baala, 
						Editor, Al Mar’a and The Woman gave a brief insight 
						about the awards and Al Mar’a magazine’s decade long 
						contribution to women’s affairs in Oman. Sandeep Sehgal, 
						Chief Executive and Alpana Roy, Executive Vice 
						President, UMS joined the dignitaries on stage for the 
						awards distribution. <br>
						The event saw a dazzling display by an international 
						troupe of Fire Dancers, who literally set the stage on 
						fire with their daring acts. A raffle draw by Travel 
						City saw a lucky winner winning a four day package to 
						Turkey. Jotun Paints held a painting competition on the 
						sidelines, with three winners winning a chance to get a 
						room painted for free. The evening ended with Chandni 
						Maniar, Business Head, Al Mar’a’s vote of thanks and a 
						sumptuous dinner. As the curtains came down on a 
						memorable evening, the guests left looking forward to 
						the 2013 edition of the awards.<br>
