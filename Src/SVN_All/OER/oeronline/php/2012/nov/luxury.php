				<b>What are your thoughts on competition in the ‘affordable 
				luxury’ segment? <br>
				</b>The competition in the mid segment market is very intense as 
				we are pitted against some of the established players who have 
				been there for many decades, if not for centuries. But within a 
				short span of 25 years we could establish ourselves as a leading 
				player in this segment. The way the brand increased its 
				marketshare testifies to the unique distinctions of our 
				products- better price and nice look. <br>
				<br>
				<b>How do you differentiate yourself from other brands in this 
				segment? <br>
				</b>Frederique Constant watches combine luxury and affordability 
				and tradition and contemporariness. High quality, 
				differentiation and precision in design and manufacture are the 
				hallmark of our products. We have a passion for design and our 
				factory in Plan-les-Ouates, Geneva- where most of the world’s 
				best known watch manufacturers are based -offers the best 
				environment for our passionate watchmakers. Each of our watches 
				is assembled by hand and extensively controlled with latest 
				equipment to ensure maximum quality. We innovate to offer 
				creativity and exceptional value. The brands philosophy is not 
				to restrict the interest in Frederique Constant watches to a 
				limited and elitist circle of connoisseurs but rather to a 
				broader selection of appreciative enthusiasts who want to enjoy 
				high quality classical watches at sensible prices. <br>
				<br>
				<b>How strong is your presence in the Middle East and what is 
				your expectation of the Omani market? <br>
				</b>At present the Middle East region represents a bigger market 
				of the total sale in the Asian region. The growth has been 
				phenomenal during the last couple of years. When I came to the 
				region in 2009, Middle East constituted only 2 to 4 per cent of 
				the total global sale; but today it has gone up to close to 12 
				per cent. The development in the region during the last three 
				years has been phenomenal thanks to our partners. The business 
				in the region is all about getting good partners. As a 
				family-owned company we have a very good relationship with all 
				our partners in the region. We are here to stay for a long time 
				and I expect our association with Khimji’s to last for a long 
				time. Frederique Constant timepieces are available in a variety 
				of ranges- a mix of classic, fashion, sporty and contemporary 
				watches-starting form RO350 till a luxury segment, a fact which 
				is sure to appeal to the savvy shoppers in Oman.<br>
