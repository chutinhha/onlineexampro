						Time to act</font></b><p align="justify" style="margin: 3px 10px">
						<b>
						<font face="Verdana" size="2">
                		<br>
						Clive Freeman, COO, Landmark Group – Oman speaks to 
						Muhammed Nafie about the Landmark Group’s Beat Diabetes 
						walkathon on November 16, 2012 and the ongoing success 
						of the campaign<br>
                &nbsp;</font></b><p align="center" style="margin: 3px 10px">
						<img border="0" src="images/health-nov-12-1.jpg" width="400" height="300"><p align="justify" style="margin: 3px 10px">
						&nbsp;<p align="justify" style="margin: 3px 10px">
				<font face="Verdana" size="1">
                		<b>The annual campaign against diabetes has been part of 
				you CSR initiative since 2009. How effective has the campaign 
				been in generating awareness?<br>
				</b>In 2009, Landmark Group launched the Beat Diabetes 
				initiative in UAE as part of its long term Corporate Social 
				Responsibility (CSR) strategy. Aimed at creating awareness about 
				the causes, effects and dangers of this condition, the 
				initiative has gathered tremendous momentum over the last four 
				years. The initiative was extended to five new countries 
				including Bahrain, Oman, Kuwait, Qatar and India in 2010 and 
				last year the group extended the initiative to KSA, and in the 
				process organised a series of walkathons in six countries, which 
				drew the participation of over 35,000 people, and over 40,000 
				people took the free blood glucose tests. The initiative also 
				witnessed several activities all year-around, with a common goal 
				of ‘spreading awareness about the condition’. <br>
				<br>
				In 2011, Landmark Group joined hands with the International 
				Diabetes Federation (IDF), becoming their first retail corporate 
				supporter partner.<br>
				<br>
				<b>Can you explain the various activities that the group will be 
				conducting as part of the campaign?<br>
				</b>Several community outreach activities are organised all year 
				round in the countries the campaign is spread in and all these 
				cumulate with the Beat Diabetes Walkathons in November. In Oman, 
				we are reaching out to various private corporations, 
				universities, labour camps and cities like Sohar and Salalah to 
				spread awareness about diabetes. Free blood-glucose test and 
				consultation camps are already underway in most of these places. 
				We have also set up testing centre’s in various ministries like 
				Ministry of Health and TRA offices.<br>
				<br>
				How was the response from the public been to the Beat Diabetes 
				walkathon over the years?<br>
				We have received tremendous response from the local communities 
				in all the seven countries the campaign currently runs. In Oman, 
				since the launch in 2010, the last two editions of walkathon had 
				seen participation from over 5,000 people and more than 4,000 
				residents took the test at various test camps.<br>
				<br>
				<b>What are your plans to expand the reach of the campaign to 
				more people in Oman and other GCC countries?<br>
				</b>Since the launch of the initiative in 2009, the campaign has 
				spread to seven countries. And in the coming years we will 
				extend this to many other countries where the Landmark Group 
				operates. This year in Oman we will be conducting blood glucose 
				testing in various places and will continue to reach out to 
				private organisations, universities and government offices.<br>
				<br>
				<b>Do you want to make more public organisations and community 
				groups involved in the campaign?<br>
				</b>The Beat Diabetes initiative is a public service initiative 
				by the Landmark Group. The goal of the initiative is to spread 
				awareness about diabetes in the countries where the Group 
				operates. <br>
				<br>
				Since its inception, the Beat Diabetes initiative has worked 
				with local governments, industry partners and several support 
				groups, all with the common motive of spreading awareness. In 
				Oman our partners have been Al Raffah hospital, Johnson &amp; 
				Johnson, Fitbody and Oasis water. In the future, we will 
				continue to partner with other like-minded organisations, to 
				ensure that we reach out to as many people as possible.&#8194;</font></td>
