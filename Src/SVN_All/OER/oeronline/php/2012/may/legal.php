						<td valign="top">
						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">
						Tax on overseas dividends<br>
						</font><font face="Verdana" size="2">
                		<br>
						<img border="0" src="images/legal-may-12-1.jpg" width="127" height="134" align="right" hspace="12" vspace="3">By 
						James Harbridge<br>
						<br>
						According to a recent Supreme Court judgment, dividends 
						obtained by a company in respect of its shareholding in 
						another company will not be taxable<br>
&nbsp;</font></b><p align="justify" style="margin: 3px 10px" dir="ltr">
				<font face="Verdana" size="1">
				<br>
				Over the last couple of years, I have been working with two 
				Omani corporate clients trying to ensure that, as per the Law, 
				they are not taxed on overseas dividends which they received in 
				2002, 2003 and 2004.<br>
				<br>
				I am pleased that the Supreme Court has now recently made a 
				ruling in favour of both these two entities (i.e. they will not 
				be taxed on the overseas dividends which they received in the 
				above-mentioned tax years). In this way, the Supreme Court 
				judgment has given effect to Article 8(bis) of Royal Decree 
				47/81 as amended, which states that dividends obtained by a 
				company in respect of its shareholding in another company will 
				not be taxable.<br>
				<br>
				The effect of the Supreme Court ruling also makes it clear that 
				the definition of “Company” in Article 2(4) of Royal Decree 
				47/81 (as amended) covers any company, regardless of whether it 
				is an Omani or foreign company. As a result, the Supreme Court 
				ruled that these dividends from overseas were not taxable in the 
				hands of the Omani entities.<br>
				<br>
				Another effect of the two recent Supreme Court judgments is that 
				it is now clear that Supreme Court case 42 and 44/04 cannot be 
				applied to the scenario of overseas dividends. This 2004 case 
				was about management income received in tax years 1994, 1995 and 
				1996 by an Omani company for managing a UAE entity. The judgment 
				in 2004 was that the management income was taxable pursuant to 
				the general rule in Article 8 that income is taxable.<br>
				<br>
				However, Article 8(bis) has always said that dividends are an 
				exception to the general rule, and that is why I believe the 
				Supreme Court decided in its recent judgments that the 2004 case 
				could not apply to the respective cases of my two clients.<br>
				<br>
				Furthermore, Article 8(bis) became effective law on January 1, 
				2000, which is another reason why the 2004 case about 1994-1996 
				could not be applied to the tax years 2002-2004. This is in 
				accordance with the general rule that laws cannot be 
				retroactive.<br>
				<br>
				In addition, there was a 2008 Supreme Court judgment which 
				declared that overseas dividends were taxable in the hands of 
				Omani companies. However, like case 42 and 44/04, the tax years 
				in question in the 2008 litigation were 1997 and 1998. In other 
				words, the 2008 case, like the 2004 case, was about tax years 
				pre-dating the creation of Article 8(bis).<br>
				<br>
				It should be noted that these 2012 Supreme Court judgments can 
				only apply to the end of tax year 2009, as the new Income Tax 
				Law – effective for the tax year 2010 and beyond – states in 
				Article 115 that, “…in determining the taxable income for any 
				tax year, the following shall be exempted from tax: (A) 
				Distribution of profits obtained…from the ownership of shares, 
				parts or participation in the capital of any Omani company.”<br>
				<br>
				As a result, dividends received from overseas in tax year 2010 
				and beyond will be taxable. <br>
				But the Supreme Court’s two recent judgments appear to have 
				clarified the position in respect of the tax years up to and 
				including 2009.</font></td>
