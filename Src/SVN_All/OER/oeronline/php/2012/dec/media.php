						<p align="justify" style="margin: 3px 10px"> 
					<b><font face="Verdana" size="4" color="#C00000">
					Mediating divergent views</font></b><p align="justify" style="margin: 3px 10px"> 
					<font face="Verdana" size="2"><b><br>
						</b></font><b><font face="Verdana" size="2">Incisive 
					analysis, impartiality and value have helped Mediate to 
					establish itself as a credible intelligence tool on the 
					media industry, says Eihab A Abutaha, CEO Communications, 
					SABCO Group. An OER report</font></b><p align="justify" class="style13">
				&nbsp;<p align="justify" class="style13">
				<font face="Verdana" size="1">
				<img border="0" src="images/media-dec-12-1.jpg" style="border-style: solid; border-color: #808080; " align="right" hspace="7" vspace="3"></font>The media industry in Oman has largely been cagey about putting 
				itself under the arc light but all that changed one fine day in 
				2010 with Mediate – a publication from the SABCO group which 
				analysed the media, listed media and related companies and gave 
				a 360 degree view of the industry. Over the last three years 
				Mediate has established itself as an intelligence tool on the 
				media and communications industry.<br>
				<br>
				Eihab A Abutaha, CEO Communications, SABCO Group and the man who 
				conceived the idea of Mediate gives an insight into the reasons 
				for launching such a product, “There were three things that 
				inspired Mediate, one was my love for the media world as I grew 
				up in Oman at a time when the media was not well developed. 
				Second was the sheer frustration with the constant comparisons 
				regarding advertising standards in Dubai and Oman. People who 
				had no clue about advertising preached about it. Oman has a far 
				more developed media market than people credit it for and it is 
				unfair to compare two countries because creativity is rooted in 
				the local culture of a country and something that appeals to a 
				foreign sensibility may not work here. Third and probably the 
				most important point was that I am a true believer that 
				commercial success is a by product of the service that one 
				delivers and I felt that the time had come to develop a product 
				like Mediate.”<br>
				<br>
				In layman’s terms, Mediate is a roadmap to the media landscape. 
				The catchy name sounds apt and natural today, but it was the 
				result of much deliberation and thought. The germ behind the 
				name was the fact that the media industry was constantly 
				mediating between various sides, be it clients and customers or 
				readers and advertisers.<br>
				<br>
				The idea of Mediate elicited strong reactions. On the one hand 
				there were a large number of people who were sceptical about its 
				prospects, while there were others who encouraged such an idea. 
				Says Eihab, “I am happy to state that in a short span of time 
				the product has established its credibility in the market.” 
				Mediate started with a diversified reflection of the industry 
				and brought honest quotes and opinions from various quarters 
				like leading newspapers, advertisers and agencies. “I still 
				remember a controversial but interesting quote from Ahmed Al 
				Zadjali, CEO, Muscat Press and Publishing House in the first 
				edition venting his frustration when he said – ‘Agencies are 
				bloodsuckers and zombies, they do not invest in media planners 
				or media buyers, they do not have a database of research 
				support, moreover they do not do their homework, if a company 
				wants to launch a campaign they simply put all the publications 
				in the media plan by the agencies.’ This was countered by 
				Mohammad al Farei, managing director of the Zeenah Group, who 
				felt that this was an unfair criticism of advertising 
				agencies.”. Mediate has consistently striven to reflect a 
				polarity of views in the publication.<br>
				<br>
				<b>Evolutionary process<br>
				</b>The 2013 edition of Mediate marks its fourth edition and it 
				has evolved with each new iteration. The product has been 
				enhanced by strengthening the analytical section on a number of 
				emerging media and communication channels including radio, 
				outdoors, social media and public relations. It has also evolved 
				physically becoming more user friendly and with a sharper design 
				sensibility. The strength of Mediate is underscored by the fact 
				that requests for copies come from all across the world. “We get 
				requests for hundreds of copies from a large number of countries 
				including Kuwait, Saudi Arabia, France, UAE, India, Qatar, 
				Thailand and China. These requests come from investment houses, 
				media associations, industrial chambers etc, which show their 
				interest in Oman and they are referring to Mediate as a source 
				of information and guidance.”<br>
				<br>
				Coming from an established media house, a few people raised 
				queries about the impartiality of such an effort. Eihab counters 
				this vehemently, “Who else is going to do it, any media house 
				that has journalists would be able to apply impartiality and 
				talk about the industry in an unbiased manner. We see CNN and 
				BBC talking about each other and their views are respected. 
				Mediate is a product by the industry and for the industry. 
				Credibility can be measured overtime and if the product was not 
				credible it would have failed by now.” Mediate is a platform 
				that cherishes its integrity and provides a rational and 
				analytical tool backed by statistics and research. Ironically, a 
				number of media and research agencies use Mediate to verify 
				their numbers.<br>
				<br>
				Moreover, the product does not distinguish between large or 
				small organisations, with every company getting the same space 
				and design treatment. The publication uses a predefined set of 
				criteria to write media companies. “What we make sure is that 
				Mediate does not pass judgement on any particular company. We do 
				not judge another media and neither is it their role to judge 
				us, as that is the responsibility of a media planner. The 
				analysis brings out the changing media landscape in Oman.”<br>
				<br>
				As the media industry is too busy promoting clients, products 
				and writing about other things, it usually misses out on the 
				opportunity to reflect upon itself. Mediate allows the media to 
				train the spotlight on itself once a year. “It allows them to 
				see as to who they are, what they do and to write their profile. 
				In today’s day and age it is important for both companies and 
				individuals to see as to how they define themselves.”<br>
				<br>
				The model followed by Mediate integrates various media that are 
				relevant to audience like clients and media, with dedicated 
				sections on each one of them. Key contacts and information on 
				companies is listed in a reader-friendly format. “Imagine you 
				are a new media house or client in town, Mediate helps you 
				understand the entire media landscape in minutes, which would 
				otherwise have taken you years.” <br>
				<br>
				<b>High on business<br>
				</b>Realising the value of Mediate a number of clients from 
				outside the media industry have started advertising in Mediate. 
				These include high-end luxury products, banks, cars, automotive 
				companies, telecom players, FMCG, industrial, government 
				associations and family owned conglomerates. “Mediate reaches 
				out to every single organisation in Oman and is used by two 
				kinds of people – the CEOs and people handling communications. 
				So it makes sense to advertise in Mediate, moreover our clients 
				have stayed with us over the years.”<br>
				<br>
				While a lot has been achieved by Mediate in the last three 
				years, the 2013 edition promises to be bigger and better. “We 
				are looking at enhancing the product by bringing in a number of 
				innovations. For example, we are working on creating an android 
				based application for Mediate. This will help people to access 
				the publication on their phone and tablets, giving them a chance 
				to access the product through different avenues.” Mediate 2013 
				will also focus on companies specialised in new media such as 
				social media, online and smartphone application development. The 
				product is expected to hit the market in February 2013.<br>
				<br>
				SABCO Media offers multimedia platforms like radio, outdoors and 
				print. The company is in the process of integrating its multiple 
				offerings. The construction of new roads in strategic areas like 
				Al Ansab and Amerat has opened up new opportunities and 
				possibilities for outdoors, and the company plans to build on 
				them. “In radio we are upgrading our broadcast capacity. Our 
				differentiator and success factor in radio is the fact that we 
				are sensitive to market demands.” The company is currently 
				running one of the largest readership surveys for its weekly 
				magazine Y. The feedback will be used to understand as to what 
				people feel about its content, design, distribution look and 
				feel. A finger on the pulse of its readers and clients is what 
				differentiates a media product and looking back one can 
				confidently state that Mediate has struck that right balance.&#8194;<br>
