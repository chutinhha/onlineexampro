				<b>Fund for development of SMEs<br>
				</b>The Ministry of Commerce and Industry has set up a new Small 
				and Medium Enterprises (SMEs) Development Fund with an initial 
				capital of RO100mn. This initiative is primarily targeted at 
				Omani school and college students with the aim of inculcating in 
				them an entrepreneurial spirit and encouraging them to pursue 
				self-employment opportunities.<br>
				<br>
				Raphael Parambi, CEO of National Company for Projects and 
				Management (NCPM), which is expected to administer the fund, 
				said the project would have hugely beneficial implications for 
				the government’s SME development objectives.<br>
				<br>
				The Fund’s seed capital of RO100mn is drawn from investments by 
				private sector pension funds, financial institutions and a 
				couple of large business groups.<br>
				<br>
				Over the next 10 years, over 500,000 students will be trained 
				and encouraged to start SMEs by training, nurturing, monitoring 
				and legitimising services. The project has the potential to 
				generate thousands of small and medium enterprises, as well as 
				contribute around RO1bn in value addition to the economy.<p align="justify" class="style13" dir="ltr">
