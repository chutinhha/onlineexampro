				<b>In-country value keeps everything ‘inhouse’<br>
				</b>In-Country Value (ICV) is defined as the total spend 
				retained in country that benefits business development, 
				contributes to human capability development and stimulates 
				productivity in Oman’s economy. Towards this end, Petroleum 
				Development Oman (PDO) set itself some significant stakes in the 
				ground and began to spend in local service provision and local 
				manufacturing out of its total capital investment, which is in 
				excess of $4bn a year.<br>
				<br>
				Raoul M Restucci, managing director, PDO, says that as per the 
				company’s ICV proposition, the goal is to dramatically increase, 
				to approximately 50 per cent at its manufacturing requirements 
				and in excess of 75 per cent in services, up to 90 per cent in 
				some cases, across the entire oil and gas value chain by 2020.<br>
				<br>
				So in financial terms it equates to an annual increment of 
				$100mn in manufacturing and $100mn in services, all targeted for 
				local development. If we take the existing spend plus PDO’s 
				annual targets, local spend will exceed $3bn by 2020, adding 
				considerable value to the gross national product, on a direct 
				and indirect basis.<br>
