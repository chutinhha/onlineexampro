				also down to the intense public dislike of the man viewed by 
				many as the “president of the rich” who had swept to victory in 
				2007 with a huge mandate to change France. The majority of 
				French people felt he had failed to deliver on his promises, and 
				was criticised for his ostentatious display of wealth, favouring 
