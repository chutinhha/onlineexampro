						<p align="justify" style="margin: 3px 10px"> 
					<b><font face="Verdana" size="4" color="#C00000">
					NBO Golf Classic to bring The European Tour to Oman</font></b><p align="justify" style="margin: 3px 10px"> 
					<font face="Verdana" size="2"><b><br>
						</b></font><b><font face="Verdana" size="2">Golf Classic 
					2013 promoted by NBO will be the first Challenge Tour event 
					of its kind in the GCC region</font></b><p align="justify" class="style13">
				&nbsp;<p align="center" class="style13">
				<font face="Verdana" size="1">
				<img border="0" src="images/sport-dec-12-1.jpg" style="border-style: solid; border-color: #808080; " width="500" height="269"></font><p align="justify" class="style13">
				&nbsp;<p align="justify" class="style13">
				The Sultanate will make its debut on the European Challenge Tour 
				when the National Bank of Oman Golf Classic is staged for the 
				first time at Almouj Golf, The Wave Muscat, from October 24 to 
				27, 2013. Promoted by National Bank of Oman, the tournament will 
				be the first Challenge Tour event of its kind not only in the 
				Sultanate but also in the GCC and will offer a total prize-money 
				of $300,000.<br>
				<br>
				With its place on the 2013 calendar (confirmed as the last event 
				on the calendar before the season-end Challenge Tour Grand 
				Final) the National Bank of Oman Golf Classic is set to attract 
				a powerful field of 84 professionals with a further 30 invites 
				available to the title sponsors, including ten for low handicap 
				amateurs.<br>
				<br>
				From the professional invites to the National Bank of Oman Golf 
				Classic, five will be allocated to other Challenge Tour events 
				in return for amateur invites on a reciprocal basis, allowing 
				Arab Nationals, subject to handicap requirements, to compete in 
				other Challenge Tour events during the season. This will provide 
				great opportunities for golfers throughout the Arab world to get 
				international experience wherever the Challenge Tour goes.<br>
				<br>
				“As Title sponsor for the next three years, National Bank of 
				Oman looks forward to welcoming the players and officials for 
				the first ever Challenge Tour event in Oman,” said Salaam Al 
				Shaksy, NBO’s CEO, at a press conference, held at Almouj Golf, 
				The Wave Muscat.<br>
				<br>
				He added, “At National Bank of Oman, we are proud of the steps 
				Oman has taken with the launch of Almouj Golf - The Wave Muscat, 
				the first Professional Golf Association (PGA) championship 
				course in the Sultanate. With the support of the Oman Golf 
				Committee - general secretary, Sayyid Arqam Al Busaidi, Oman 
				will become the 45th country to be visited by the European 
				Challenge Tour.<br>
				<br>
				Alain de Soultrait, director of the European Challenge Tour said 
				the players and officials alike were excited at the prospect of 
				staging an event in the GCC for the first time in the Challenge 
				Tour’s 26 year history. “We are absolutely delighted to be 
				bringing the Challenge Tour to Oman and these are exciting times 
				for us as it is the first time we have ever visited this part of 
				the world,” he said. As the penultimate event of the 2013 
				Challenge Tour season, the National Bank of Oman Golf Classic 
				will be a crucial tournament for our players and even more so 
				thanks to the significant prize fund”.<br>
				<br>
				In 2012 the Challenge Tour played 26 events in 18 countries for 
				a combined prize fund of over $7.1mn, while only four of those 
				events offered a greater prize fund than the new tournament – 
				and with the event also providing places in the field to 
				amateurs from throughout the region. The 2013 National Bank of 
				Oman Golf Classic will offer the best Arab golfers an 
				opportunity to experience playing alongside future European Tour 
				stars.</td>
