						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">
						Augmenting water production</font></b><p align="justify" style="margin: 3px 10px">
						<b>
						<font face="Verdana" size="2">
                		<br>
						The expansion plant of ACWA Power Barka which is 
						expected to be operational in one year’s time will be 
						able to produce 10 MIGD of potable water</font></b><p align="justify" style="margin: 3px 10px">
						&nbsp;<p align="center" style="margin: 3px 10px">
						<img border="0" src="images/inthenews-dec-12-1.jpg" width="500" height="333"><p align="justify" style="margin: 3px 10px">
				<font face="Verdana" size="1">
				<br>
				ACWA Power Barka, the leading IWPP leader in the region, held a 
				groundbreaking ceremony for its water expansion project in 
				Barka, recently under the auspices of Mohammed al Mahrouqi, 
				chairman of Public Authority for Electricity and Water (PAEW). A 
				number of dignitaries from various ministries, company officials 
				and guests attended the ceremony.<br>
				<br>
				The project is part of the expansion plans of ACWA Power and 
				once ready the expansion plant will produce 10 MIGD (million 
				imperial gallons per day) of potable water. This Independent 
				Water Project (IWP) will be developed on a Build, Own and 
				Operate (BOO) basis adjacent to ACWA Power Barka’s existing 
				plant. The existing desalination facility has been producing 20 
				MIGD of potable water since 2003 and the new plant is expected 
				to catapult the production levels to 30 MIGD. The additional 
				desalinated water capacity produced by the expansion project 
				comes as a respite as it will help reduce the projected 
				shortfall of the Sultanate’s water production capacity.<br>
				<br>
				Commenting on the occasion, Mohammad bin Abdullah Abunayyan, 
				chairman, ACWA Power Barka said, “The expansion project comes at 
				an opportune time as the demand for potable water in Oman is 
				putting pressure on the existing capacities. The additional 
				water production will help match the demand that is estimated to 
				be rising at 8 per cent per annum. The projected shortfall in 
				the water desalination production capacity of the Sultanate will 
				surely be augmented with the help of this new project.”<br>
				<br>
				The new plant will be the first of its kind and will reuse 
				seawater heat-rejection from the existing desalination process 
				for potable water capacity addition.<br>
				<br>
				Nadeem Rizvi, CEO of ACWA Power Barka, reaffirmed the commitment 
				of ACWA Power Barka to the power and water sector in Oman in his 
				comments, saying, “We are proud to undertake a project which 
				will give us a chance to demonstrate our commitment to the 
				Sultanate by contributing to the longevity of potable water 
				supply.”<br>
				<br>
				Continuing further, he said, “In the medium to long-term, this 
				expansion project will prove to be a well-sought after solution 
				to balance shortfalls of current desalinated water production 
				capacity in the Sultanate. We will continue to play a larger 
				role in the water and power sector in Oman with innovative 
				solutions that create value.”&#8194;</font></td>
