				Gulf region is being frequented by wealthy tourists who have a 
				penchant for golf and many of them are being drawn to the 
				Sultanate as the reputation of the country’s golf courses 
				continues to increase. And thanks to a string of green golf 
				courses which have been coming up in the Sultanate, Muscat has 
				made its way to the world’s golfing map slowly but steadily.<br>
				<br>
				Since its conception in 2005, OER CEO Golf has been a pioneering 
				initiative from Oman Economic Review and United Media Services, 
				its publisher. The level of participation seen in OER CEO Golf, 
				which witnesses an elite gathering of industry leaders coming 
				together for the Sultanate’s biggest networking event and golf 
				experience only goes on to show the importance of this event in 
				Oman’s corporate calendar.<br>
				While previously, the Sultanate had only brown golf courses to 
				boast of, today the country has a number of full fledged 18-hole 
				PGA green golf courses designed by world renowned golfers. The 
				9th Edition of OER CEO Golf will be held on one such golf 
				course, the Greg Norman-designed Almouj Golf in The Wave Muscat.<br>
				<br>
				OER CEO Golf 2013 offers revitalisation for Oman’s corporate 
				heavy weights, giving them an opportunity to network and compete 
				on the greens. There are few events of this nature that can 
				boast of such variety and all-round entertainment.<br>
				<br>
				While out on the greens, the itinerary will include a game of 
				golf and high powered business networking, the evening’s 
				programme highlights include a gala dinner, world-class 
				entertainment, socialising prize distribution, and much more.<br>
				<br>
				The sponsors for OER CEO Golf 2013 include Nawras Business which 
				is the main presenter while the support partners are Aggreko and 
				Times of Oman and Al Shabiba newspapers. Rolex from Khimji’s 
				Watches is the official time keeper. Talentz is the category 
				partner for AV, Light &amp; Sound.<br>
				<br>
				“Golf has always been a key sport in the corporate world and 
				Nawras is pleased to be a partner in promoting golf for the 
				business community in Oman,” says Monther Al Mamari, Director 
				Business Sales. Continuing further, he says, “Nawras has been 
				the proud presenter of the annual OER CEO Golf event for the 
				past four years and we are excited to continue on the same 
				course for 2013. We appreciate the efforts made by UMS in 
				bringing this event together to continue spreading knowledge of 
				the game while encouraging interest from both golfers and 
				non-golfers alike.”&#8194;</td>
