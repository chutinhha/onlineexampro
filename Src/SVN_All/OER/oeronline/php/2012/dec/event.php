						<p align="justify" style="margin: 3px 10px">
						<strong><span class="style27">Francois van Coppenhagen 
						wins BankDhofar Pro-Am Tournament</span></strong><p align="justify" style="margin: 3px 10px">
						&nbsp;<p align="justify" class="style13">
						<b>The first edition of BankDhofar Pro-Am Golf 
						tournament was held at Muscat Hills Golf &amp; Country Club 
						on November 7 and 8</b><br>
						<br>
						&nbsp;<p align="center" class="style13">
						<img border="0" src="images/event-dec-12-1.jpg" width="500" height="231"><p align="justify" class="style13">
						<br>
						Francois van Coppenhagen, a 23-year old PGA Professional 
						from Sahara Kuwait Resort, shot a 2 under par 70 and +7 
						points in the Modified Stableford to win the inaugural 
						BankDhofar Pro-Am and a cheque for RO500 at the first 
						professional golf event in the Sultanate which took 
						place at Muscat Hills Golf &amp; Country Club on November 7 
						and 8, 2012.<br>
						<br>
						The winning team was the Crestlink Team from Qatar which 
						was led by professional Peter Bailey (Doha Golf Club) 
						and featured amateur players Chris Duffy, Gregg Krahulec 
						and Hiram Diggines. They scored an excellent 87 
						Stableford points, pushing the Audi team of Professional 
						Simon Westney and team members – Latif Razek, Karen 
						D’Amours and Abe Steiginga who also scored 87 points, 
						into second place.<br>
						<br>
						After incredibly consistent play and a result of +7 
						points, Francois van Coppenhagen prevailed against the 
						rest of the field and won the tournament. “It is a great 
						honour to be the first winner of the inaugural 
						BankDhofar Pro-Am, which is the first professional golf 
						event staged in Oman. I look forward to returning to 
						Oman next year to defend my title”, said Francois after 
						the prize giving.<br>
						<br>
						Professional runner-up with a result of +6 was Muscat 
						Hills-based professional Alex Day with Cormac Flannery, 
						head professional at the Royal Golf Club, Bahrain 
						finishing third on +2.<p align="justify" class="style13">
						<br>
						Tony Mahoney, CEO of BankDhofar, awarded the 
						professionals during a special prize-giving ceremony, 
						closing the successful two-day professional golf event 
						at Muscat Hills Golf &amp; Country Club. “My thanks to the 
						guests and professionals who came together to make this 
						a wonderful event. BankDhofar are proud to support the 
						awareness and development of golf in the Sultanate and 
						look forward to announcing some new youth initiatives 
						next year”, said Tony Mahoney, CEO BankDhofar in his 
						opening speech. The inaugural BankDhofar Pro-Am saw a 
						field of 23 teams, consisting of four players. Each team 
						was led by a regionally based PGA professional who was 
						joined by three amateurs, entering in teams under 
						company names.</td>
