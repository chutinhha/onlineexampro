						<td valign="top">
						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">
						Shooting in the dark</font></b><p align="justify" style="margin: 3px 10px">
						<b>
						<font face="Verdana" size="2">
                		<br>
						The insurance industry in Oman is marked by a lack of 
						innovation, coordination and cooperation. Everyone is 
						saying the boat is rocking, but there is no concerted 
						effort to steer it clear. A report by Visvas Paul D 
						Karra<br>
                &nbsp;</font></b><p align="center" style="margin: 3px 10px">
						<img border="0" src="images/insurance-oct-12-1.jpg" width="400" height="266"><p align="justify" style="margin: 3px 10px">
				<font face="Verdana" size="1">
				<br>
                		The Sultanate’s insurance industry is on the cusp of 
				change due to the poor performance brought about by losses in 
				the section of the underwriting business namely the motor 
				claims. Incredibly, most insurance companies seem to be caught 
				in a pack mentality and everyone is doing what the other is 
				doing with slight cultural differences depending upon the 
				company and the management. But the type of business that is 
				available in the market and what these companies are doing is 
				fragmented with a mixture of retail, SME segment and commercial 
				risks.<br>
				<br>
				One of the reasons for this is the small size of the market. In 
				India, for example, the population is touching 1.2 billion 
				whereas the whole population of Oman is a little over a three 
				million. The market premium in India excluding life insurance is 
				around $13-14bn with 23 players. But in Oman, the premiums 
				including life, just touch $700mn with the same number of 23 
				players. Out of this around 40 per cent is in the motor 
				insurance.<br>
				<br>
				Industry experts point out the competition is too intense which 
				means that all hands are in the same pie and the pie does not 
				seem to be getting any bigger, atleast in the near future. To 
				top it, the gains that could be accrued by any company are being 
				literally wiped out by unabated losses in the motor insurance 
				segment. Effectively it means that insurance companies are 
				bleeding away unless some remedial steps are taken.<br>
				<br>
				According to a report by Business Monitor International, Oman 
				will likely remain home to an insurance sector that is small, 
				fragmented and highly competitive. In recent years, retention 
				rates are low, often well below 50 per cent. What is of interest 
				to note is that over the last year or so, motor-related claims 
				have been unacceptably high, notes BMI.<br>
				<br>
				None of the local companies have the benefits of economies 
				neither of scale, nor, with the clear exception of al-Ahlia 
				which is an affiliate of global non-life major RSA Insurance, 
				are any owned by major shareholders that have a clear competence 
				in insurance.<br>
				<br>
				<b>Motor insurance<br>
				</b>Motor insurance has become a double-edged sword for almost 
				all the insurance companies in the country. One the one hand, 
				motor business seems to be occupying a fat portion of the books 
				– actually more than 40 per cent of the total premiums – but on 
				the other hand, it is gnawing at the bottom lines of the 
				companies mainly due to high number of claims and the steep 
				costs of the claims.<br>
				<br>
				Says Taher T Al Heraki, managing director and CEO, of Dhofar 
				Insurance, “We have faced problems with the motor insurance but 
				we tried to improve the way we underwrote the business within 
				the regulatory limits. But the number of claims from minor 
				accidents continued to rise and we are working hard to reduce 
				the expenses by controlling the costs of the claims. But 2012 
				has been much better than 2011 without a doubt.”<br>
				<br>
				However, some costs are beyond the control of the insurers like 
				the costs of the spare parts which is fixed by the car dealers. 
				Another sore point for the insurers is the regulatory clause 
				which stipulates that all new cars which meet with accidents in 
				the first year have to be given to the dealer for repairs.<br>
				<br>
				With the increasing number of road accidents, the legal costs 
				and the blood money have also gone up. “We feel sorry for the 
				increasing number of people who die in road accidents but for us 
				as a company, our main aim is to reduce the claim costs. And I 
				believe that one of the ways to do it is create a system, a data 
				base of insurance claims. A person who has accident claims with 
				one insurer goes to another insurance company the next. But the 
				other company has no history of the earlier claims. So it is 
				important for all concerned parties, which includes the 
				regulator, the police and the insurance companies to come 
				together to stem this leak,” adds Taher.<br>
				<br>
				<b>Hardening of rates<br>
				</b>“Hardening of rates is the only way forward for most 
				companies to tide over losses which arise chiefly due to the 
				motor business,” says A R Srinivasan, general manager, Falcon 
				Insurance. In the post 2008-period with the global financial 
				systems in trouble, the easy availability of capital is not 
				there anymore and the pressure on companies to perform is 
				increasing because the investment of returns as expected earlier 
				is not there.<br>
				<br>
				“Perhaps up to the year 2008, all the companies were having 
				investments or the other income which was hiding these kinds of 
				losses. But it is not possible anymore and people will have to 
				look at the core insurance business to start making money. If 
				you have been following the market, most of the insurers have 
				lost the support of major re-insurers as the market has not 
				reacted to have proper technical rating in terms of premium and 
				deductibles,” adds Srinivasan.<br>
				<br>
				It’s one of those situations where if companies continue to make 
				losses, they don’t invest in future growth, innovation and 
				training. If they don’t do that, then the industry retracts. At 
				the same time, the percentages of Omanisation have been 
				increased which requires a lot of investment for salaries, 
				training and development. Then you also talk about building a 
				certain amount of credibility in the local market. And all of 
				that can only be addressed if the market is relatively 
				profitable, which seems uncertain in the present circumstances.
				<br>
				<br>
				<b>Consolidation of market<br>
				</b>Due to the intense competition, market experts opine that 
				the sector should see some consolidation to ensure that everyone 
				stays afloat. But actually it has to be seen how it happens. 
				Because, in the last so many years, except in Bahrain, no 
				mergers have happened between local companies. The merger 
				between Royal Sun Alliance and Al Ahlia Insurance is an 
				exception because it was the merger of one international player 
				with a local company. If the regulator comes with new rules to 
				increase the capital base, that could make companies to think of 
				consolidation as opposed to raising capital.<br>
				<br>
				“Most companies are deeply entrenched in the traditional methods 
				of operations. There is a need to have vision, foresight and 
				innovation. Most companies are thinking on a need-basis as they 
				get comfortable with what they are doing,” says Gautam Datta, 
				CEO, Al Madina Insurance Co.<br>
				<br>
				Even though insurance is all about dealing with uncertainty, the 
				reason why the insurance industry is risk averse is because they 
				want a certain amount of certainty on their balance sheet. “And 
				how do you bring about the certainty, by looking at the risk 
				exposures and how the capital will be exposed,” adds Datta. <br>
				<br>
				Continuing further, Datta says, most companies are looking in 
				the rear-view mirror but we need to look ahead if we have to 
				survive in the future. We have to prepare ourselves in terms of 
				how we need to look at the risks. The public also needs to be 
				educated about the issues and importance of voluntary insurance. 
				The industry is not able to retain its capital and what is 
				retained is not put to good use. And you cannot put it to good 
				use if you don’t have the sufficient skills sets and for this 
				you need to train people, which means the industry has to 
				invest, and to invest the industry needs to make a profit. So it 
				is a Catch-22 situation.”<br>
				<br>
				<b>Raising awareness<br>
				</b>According to Srinivasan, the awareness levels about the 
				importance of voluntary insurance was minimal all these days but 
				now the situation is changing. Earlier, it was only the high 
				networth individuals, both expatriates and Omanis who used to 
				buy insurance. But these volumes were very low. In other 
				countries, bancassurance is the main form of insurance where 
				people have life insurance, home insurance and other insurances 
				and then the balance is used for daily expenses. Here it is the 
				reverse. People think of insurance, if anything is left after 
				all expenses.<br>
				<br>
				Slowly there is a cultural shift happening within the local 
				population. Now as husband and wife begin to work and move to 
				the city, they see other things happen and since both are 
				working, they are saving for the child’s education, 10 years 
				down the line. Such personal savings may snowball into a bigger 
				thing ten years down the line. So savings insurance is slowly 
				gaining and along with this, the retail insurance also will 
				gain. <br>
				<br>
				<b>New opportunities<br>
				</b>Over the last few years, especially in the last two or three 
				years, the medical insurance business is picking up as more 
				companies take medical insurance for their employees. <br>
				<br>
				AXA Oman, part of AXA Gulf and one of the largest non-life 
				international insurers in the Middle East, has registered 
				phenomenal growth. Primarily, the growth sectors are health and 
				motor insurance. Says, Deepak Kamath, country manager, Oman, 
				“Health and motor insurance are two of the fastest growing 
				sectors for us despite strong competition. In the past, AXA had 
				not been very active in these sectors primarily for two reasons: 
				the insurance premium was considered low which would have 
				resulted in our profits being eroded and secondly we were not 
				fully geared up to provide the high quality service expected by 
				our customers. Today, we have successfully addressed both these 
				issues and continue to work harder to keep our service levels at 
				international standards. I personally do not believe in selling 
				any product unless I am confident that we can deliver the best 
				possible solution and service to our customers at the right 
				price.”<br>
				<br>
				“On health insurance, it was surprising to note that many 
				customers are asking for a very limited cover with very low 
				limits, even as low as RO1,000! This is not the right way 
				forward, which in my opinion should cover the insured against 
				the unexpected and hence, has to be significant. Nowadays, there 
				is a lot more awareness among people on what they need – ranging 
				from better healthcare facilities, more options, higher limits, 
				24 hours service and much more! As AXA is able to offer all of 
				the above, we have grown significantly on our health insurance,” 
				Kamath adds.<br>
				<br>
				<b>Infrastructure business<br>
				</b>Companies like the UAE-based Oman Insurance Company eye the 
				government infrastructure spending for their growth. Spending by 
				the government on airports, roads, railways, fresh water and 
				wastewater purification plants will continue for many years. 
				“But economically, if you look at the industrial sector as such, 
				the growth is slow. For example, in the manufacturing industry, 
				growth is limited. The government is also concerned about this. 
				Even though the drive for spending is there, the growth is slow. 
				For us, as a company, engineering (infrastructure projects) is 
				where we see the immediate growth,” says Niel Brand, country 
				manager for OIC.<br>
				<br>
				One of the reasons why OIC is able to concentrate on the larger 
				businesses is because of the strong capital base provided by its 
				parent company. “We also have strong re-insurance treaties with 
				international RI companies, so we have the capacity,” says 
				Brand. The other growth area for OIC is the facultative 
				business, meaning, if the company underwrites a large project in 
				Oman, the head office will re-insure it with a large re-insurer. 
				However, occasionally the reinsurance or coinsurance is done 
				with local companies with a similar, strong rating. This keeps 
				the insurance in the country itself and it is good for Oman.<br>
				<br>
				<b>Takaful<br>
				</b>Takaful is coming to Oman at long last, after almost 22 
				years of being present in the GCC and one of the first companies 
				to get into this business will be Al Madina Insurance. It is 
				expected that takaful will add depth in the financial industry 
				and activate the industry. Despite criticism, there are many 
				advantages with takaful. Basically, it is based on certain codes 
				of ethics and concept of Sharia. Takaful requires transparency 
				and this requires a high degree of ethics in terms of 
				disclosures which means there is far more information sharing. 
				Datta believes that there is a segment of population for whom 
				this will be catering to their spiritual requirements.<br>
				<br>
				Datta adds that on the whole, the change is positive for the 
				economy and a lot of money will come out as investments. He also 
				disclosed that Al Madina is in the process of getting its 
				takaful license. “Our company was set up with the concept of 
				akaful and we are the only company in the GCC which is 
				converting its full business to Takaful which should provide 
				confidence to investors. We will be in a far better position to 
				declare surpluses provided we get the product pricing right and 
				correct underwriting,” adds Datta while saying they will be able 
				start operating from early next year as a takaful company.<br>
				<br>
				<b>Insurance intermediaries<br>
				</b>The insurance intermediaries or commonly known as brokers 
				have begun to play a major role in the insurance industry. And 
				approaching the right kind of intermediary is extremely 
				important for anyone who wants to buy insurance. “When life 
				catches you off guard, you are sometimes left with nothing. This 
				is why we say, get proper advice from a professional insurance 
				intermediary regarding the right type of insurance cover, sum 
				insured, values for insurance, limit of indemnity etc and choose 
				the right insurer and the right insurance for your needs,” says 
				Dr J Retnakumar, CEO, Gargash &amp; Trade Links Insurance Services.<br>
				<br>
				“Individuals, businesses and organisations are exposed to 
				various physical and financial hazards. A professional insurance 
				intermediary will help you understand this and help advise 
				possible measures to counter these impacts with risk management 
				services. Insurance covers and policies evolve continuously. A 
				professional insurance intermediary can help you in getting 
				access to up to date insurance products and services,” he says 
				further.<br>
				<br>
				Gargash &amp; Trade Links is a joint venture between the leading 
				National Chartered Insurance Intermediary, based in Dubai and 
				Tradelinks Group of Oman. Recently the company opened a new 
				branch in Sur and is expected to have a presence in the other 
				major cities of Oman.<br>
				<br>
				Retnakumar says his company has plans to open kiosks in the 
				hypermarkets and thus be closer to customers. The company also 
				has plans to develop a special insurance package for SMEs as 
				well as launch a new health product and another one for the 
				low-income expatriates.&#8194;<br>
