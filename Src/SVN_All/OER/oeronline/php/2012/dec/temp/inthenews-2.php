						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">
						EXPLORING INVESTMENT OPPORTUNITIES</font></b><p align="justify" style="margin: 3px 10px">
						<b>
						<font face="Verdana" size="2">
                		<br>
						The second edition of Oman Investment Forum 2012 will 
						showcase Sultanate as one of the most dynamic 
						destinations for investment opportunities</font></b><p align="justify" style="margin: 3px 10px">
						&nbsp;<p align="center" style="margin: 3px 10px">
						<img border="0" src="images/inthenews-oct-12-3.jpg" width="250" height="375"><p align="justify" style="margin: 3px 10px">
				<font face="Verdana" size="1">
				<br>
				Subsequent to an exceptionally successful maiden edition 
				displaying Oman as an investment destination, the second edition 
				of Oman Investment Forum 2012 will be held between on October 8 
				and 9 at the Al Bustan Palace - a Ritz-Carlton Hotel Muscat. <br>
				<br>
				Presented by MEED Events and their strategic event partner, the 
				Public Authority for Investment Promotion &amp; Export Development 
				(PAIPED), the forum will bring together government 
				representatives, leading industry figures and the international 
				business community on a singular platform to discuss the growth 
				of Oman’s economy. <br>
				<br>
				The forum will be an exclusive opportunity to be privy to the 
				latest on Oman’s economy and the updated expenditures for Oman’s 
				major sectors from 2013 to 2015. Government officials will 
				reveal their visions, strategic roadmap and investor incentives 
				along with providing an overview of current and new business 
				opportunities in Oman’s projects market, currently valued at 
				$55bn.<br>
				<br>
				Highlights of the event include a keynote address by HE Yahya 
				Said Al Jabri, chairman of the Special Economic Zone (SEZ) at 
				Duqm who will give a strategic overview on the key milestones 
				achieved at Duqm SEZ as well as present on the upcoming 
				infrastructure project opportunities for consultants, 
				contractors and service providers in each of the development 
				phases for Duqm SEZ.<br>
				<br>
				Ministerial representatives speaking at the event include HE 
				Salim Al Aufi, CEO, Public Authority for Civil Aviation who will 
				present on the latest developments and opportunities for Oman’s 
				regional airports; HE Dr Ali Talib al Hinai, Undersecretary 
				Planning, Ministry of Health, who will deliver an overview of 
				Oman’s healthcare facilities construction master plan and HE 
				Khalifa Bin Mubarak Al Hinai, Ministry Advisor, Ministry of Oil 
				and Gas, who will give exclusive insight on Oman’s multibillion 
				oil &amp; gas project opportunities. His Excellency Mohammed bin 
				Al-Zubair, Adviser to His Majesty for Economic Planning Affairs 
				will be the chief guest for the opening ceremony on October 8, 
				2012.<br>
				<br>
				“This edition of Oman Investment Forum will be a must-attend for 
				those interested in investment prospects in the Sultanate, as it 
				promises to be a unique networking opportunity wherein Oman’s 
				private sector and international investors will exchange their 
				views on best practice strategies for running a successful 
				business,” said Edmund O’Sullivan, chairman, MEED Events.<br>
				<br>
				Other confirmed speakers at the event include Marcos Beluco, 
				country manager-Vale; Wael Al-Lawati, CEO –Omran; Pankaj Khimji, 
				drector- Khimji Ramdas; Qais Saud Al Zakwani, deputy executive 
				director- Authority for Electricity Regulation, Oman; Mohamed Al 
				Maskeri, director general- Knowledge Oasis Muscat; Angus 
				Hindley, research director- MEED Insight and Dr Axel Goehler, 
				honorary consul-honorary consulate of the Hashemite Kingdom of 
				Jordan among others.<br>
				<br>
				Spread across two days, the conference will encompass 
				macroeconomic trends and outlook for Oman’s economy as well as 
				upcoming project opportunities across Oman’s tourism and real 
				estate, transport, project funding and industrial ports and free 
				zones on the first day. The next day, the discussions will 
				entail social infrastructure projects including healthcare and 
				education as well as opportunities across Oman’s oil &amp; gas, 
				power &amp; water and industrial sectors.</font></td>
