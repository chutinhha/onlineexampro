						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">
						A new bull market in Asian shares<br>
						</font><font face="Verdana" size="2">
                		<br>
						Asia continues to be the growth engine of global 
						economy, despite the recent fall in GDP growth rates in 
						some countries<br>
						<br>
						</font></b>
						<i><font color="#333333" face="Verdana" size="1">BY Matein Khalid</font></i><b><font face="Verdana" size="2"><br>
                		</font></b><font face="Verdana" size="1">
				<br>
						Even though GDP growth rates in China and India have 
						fallen, Asia is still the growth engine of the global 
						economy, with none of the systemic sovereign debt crises 
						that plague Western Europe. The easy money policies of 
						the Fed, the ECB, Bank of England and the PBOC have also 
						led to an expansion in the global monetary base that has 
						historically been correlated with bull markets in Asian 
						markets. Negative real dollar interest rates, a housing 
						recovery in the US and a growth reacceleration in China 
						are also bullish for Asia. The risks, of course, include 
						the US fiscal cliff, a traumatic Greek exit from the EU, 
						an oil shock linked to geopolitical events in the Middle 
						East akin to 1973, 1979 and 1990, or geopolitical crises 
						in the Korean DMZ, the Straits of Taiwan, Tibet, the 
						Spratley/Senkuku islands and the Line of Control in 
						Kashmir.<br>
						<br>
						Japan’s Nikkei Dow and TOPIX are some of the cheapest 
						markets in Asia relative to their own past valuation 
						ranges. As a new LDP leader Shinzo Abe could well become 
						the next Japanese PM, reflation and more aggressive 
						monetary easing by the Bank of Japan will be used to 
						offset deflation risk. I expect we could see dollar yen 
						at 86 – 88 by mid-2013 and a 100 point rise in the TOPIX 
						from its current 725 level.<br>
						<br>
						<b>$7.3 trillion economy<br>
						</b>In China, President Xi Jinping and Premier Li 
						Keqiang have been anointed by the Communist Party’s 
						Politburo to manage a $7.3 trillion economy where lower 
						interest rates are imminent. This means the M2 money 
						supply and bank loan growth will continue to expand, 
						classic leading indicators of cyclical upturns in 
						Chinese equities. China has been derated in the 
						financial markets and now trades at a mere 11 times 
						earnings. The Big Four Chinese banks are among the 
						largest banks in the world and the cheapest in Asia. 
						ICBC, Bank of China and CCB are obvious candidates for a 
						valuation rerating.<br>
						<br>
						Hong Kong is the obvious beneficiary of monetary easing 
						by the Federal Reserve due to the Hong Kong dollar peg 
						that attracts global hot money into its financial and 
						property markets. Moreover, Fed QE and President Obama’s 
						reelection led to an epic fall in US Treasury bond 
						yields, an event that slashes borrowing costs for Hong 
						Kong property developers and banks, whose shares 
						dominate the Hang Seng index. It is no coincidence that 
						Hong Kong property prices soared after both past Fed QE 
						announcements even though the Chinese government 
						deliberately defused a Mainland property bubble via 
						higher rates/restrictions that led to a vicious bear 
						markets in Shanghai.<br>
						<br>
						The year 2012 was a fabulous year for Hong Kong property 
						developers, if not for its megabank HSBC, mired in 
						Western Europe loan losses, weak investment banking 
						revenues and a money laundering scandal in Mexico that 
						has forced management into $1bn in provisions. Wharf, 
						Kerry Properties, Fortune REIT, Henderson Land, New 
						World and even Cheong Kong, Li Ka Shing’s flagship firm, 
						vastly outperformed the 16 per cent rise in the Hang 
						Seng index. I believe the Hang Seng index will fluctuate 
						in a 19000 – 23000 range next year. Moreover, tax and 
						stamp duty rises mean the Hong Kong SAR government, 
						which reports to Beijing, does not want another steep 
						property bubble. After all, Hong Kong property prices 
						lost 70 per cent after the British Crown Colony was 
						handed over to the PRC in 1997 and the onset of the SARS 
						crisis in 2004. More than three fourth of all Hong Kong 
						homeowners went into negative equity given the sheer, 
						protracted scale of the property crash. The Chinese 
						government does not want another epic property crash in 
						the first year of the Communist Party’s new rulers in 
						Beijing.<br>
						<br>
						So I would avoid Chinese developers, who do not benefit 
						from Fed QE due to hot money inflows and simply do not 
						have the pipelines/execution skills of their Hong Kong 
						peers. This means it’s best to invest only in the shares 
						of the largest, most blue-chip with the best balance 
						sheet and landbank. This is Cheong Kong, whose NAV will 
						rise after it lists its Horizon hospitality division in 
						an IPO. Cheong Kong trades at a 35 per cent discount to 
						NAV, is run by Victor Li and can well rise 20 per cent 
						in 2013 as its NAV discount narrows while it deploys its 
						excess cash in UK infrastructure. I believe LI Ka 
						Shing’s property empire will trade between HK$100 and 
						HK$130 in 2013.<br>
						<br>
						Taiwan TAEX is dominated by major technology companies 
						led by Acer, Taiwan Semiconductor, Hon Hai Precision and 
						HTC. So the massacre in PC vendor HP/Dell shares and 
						Apple’s 180 point fall since September was traumatic for 
						Taiwan, where both Acer and HTC shares were bloodied 
						after lower earnings guidance. At 17 times trailing 
						earnings, Taiwan is one of the most expensive stock 
						markets in the Pacific Basin. While the Taiwan stock 
						market has a dividend yield of 3.5 per cent, it is still 
						vulnerable to the PC/component/semiconductor glut in 
						Silicon Valley. The Taiwan TAIEX can well fall to 6500 
						if there is a broad based earnings downgrade in the tech 
						sector. It is best to only buy shares of Taiwan banks 
						such as Chinatrust.<br>
						<br>
						Singapore’s economy is exposed to global growth and 
						world trade, with exports 250 per cent of GDP. 
						Singapore’s retail sales, exports and industrial 
						production data have turned softer since September. This 
						will force the MAS to abandon its hawkish anti-inflation 
						policy and lead to a slight depreciation of the 
						Singapore dollar to 1.25 even as the Straits Times index 
						falls <br>
						to 2800-2900.&#8194;<br>
