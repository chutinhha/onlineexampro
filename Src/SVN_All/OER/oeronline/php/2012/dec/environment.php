						will be in demand globally. Projections are that the 
						demand is increasing by 20 per cent annually. Green 
						computing is certainly the next wave in computing. These 
						courses will produce green collar IT professionals who 
						can rethink, reengineer, reduce, reuse and recycle”.<br>
