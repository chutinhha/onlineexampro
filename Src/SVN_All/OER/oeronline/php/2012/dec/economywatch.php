				moderately down year-on-year, though closing up the gap versus 
				2Q12 (where they were down by 40 per cent yoy). The data remains 
				bulky but while Kuwait and Iraq construction in particular drag 
				down the total, note that year-to-date contract awards in the 
				UAE, Qatar and Saudi increased by 25 per cent yoy to $41bn. 
				Construction and infrastructure awards in the latter countries 
				also increased by 16 per cent yoy to US$18bn year-to-date. On a 
				year-to-date basis, while total awards (excluding defence 
				contracts) are down 22 per cent yoy in Saudi Arabia, this is 
				mostly due to a large drop in oil and gas project awards (down 
				84 per cent yoy to $0.8bn). In the meantime, construction and 
				infrastructure awards increased by 21 per cent yoy to $6.3bn 
				despite a soft patch over the summer months. In the UAE, the 
				past four months saw large contracts being awarded in the oil &amp; 
				gas and petrochemical sectors worth US$9bn while construction 
				and infrastructure awards increased by $3bn over the same 
				period. The latter grew by 32 per cent yoy to stand at $7.9bn 
				while total awards grew by a whopping 85 per cent yoy to stand 
				at $17.4bn year-to-September. After a strong start to the year 
				in Qatar where construction awards were up 19 per cent yoy in 
				5m12, these remained flattish over the summer lull. That being 
				said, total awards still grew by 61 per cent yoy 
