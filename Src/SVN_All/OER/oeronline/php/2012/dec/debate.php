						<p align="justify" style="margin: 3px 10px"> 
					<b><font face="Verdana" size="4" color="#C00000">
					Is Oman Future Ready?</font></b><p align="justify" style="margin: 3px 10px"> 
					<font face="Verdana" size="2"><b><br>
						</b></font><b><font face="Verdana" size="2">Oman Debate, 
					one of its kind national platform bringing the corporate 
					sector and the government together to discuss issues of 
					mutual concern that have a bearing on the Sultanate’s 
					economy, will be held on December 3, 2012 at Oman 
					Auditorium, Al Bustan Palace Ritz Carlton Hotel</font></b><p align="justify" class="style13">
				&nbsp;<p align="justify" class="style13">
				All is set for the most awaited annual business forum in 
				corporate Oman’s calendar, Oman Debate 2012 and OER Top 20 
				Awards. Organised by Oman Economic Review (OER), the leading 
				business magazine in the Sultanate, in association with the 
				Capital Market Authority (CMA) and anchored by renowned 
				television host Tim Sebastian, the founder and chairman of ‘The 
				Doha Debates’ and former BBC Hard Talk presenter, the annual 
				national debate has generated tremendous excitement over the 
				last four years in the corporate world of the country. It offers 
				a healthy atmosphere where representatives from both the public 
				and private sectors can discuss issues of mutual concern that 
				have a bearing on the Sultanate’s economy.<br>
				<br>
				Titled ‘Is Oman Future Ready?, Oman Debate 2012 will encompass 
				key themes that form the business environment of today and 
				tomorrow in Oman such as job creation, ease of doing business in 
				the country and the need for a more robust private-public 
				partnership.<br>
				<br>
				HE Ali bin Masoud al Sunaidi, The Minister of Commerce and 
				Industry will be the chief guest at the fourth edition of the 
				Debate which will be held under the patronage of HH Sayyid Tarik 
				Al Said, editor-in-chief, Oman Economic Review and HE Abdullah 
				al Salmi, executive president, Capital Market Authority.<br>
				<br>
				The Debate features an august panel of speakers from the 
				government, the Majlis as Shura (people’s council) and the 
				private sector to represent a cross section of opinions. They 
				include Manal Abdwani, director general, Ministry of Commerce 
				and Industry; Musab al Mahrouqi, CEO, ORPIC; Abdulaziz Al 
				Balushi, CEO, Ahlibank; HE Tawfiq al Lawati, member, Majlis Al 
				Shura &amp; the Economic Committee of Majlis Al Shura ; Henk Pauw, 
				CEO, Sohar Aluminium; Marcos Beluco, CEO, Vale Oman Pelletising 
				Co; Stephen Thomas, CEO, Renaissance Services; Ahmed al Wahaibi, 
				CEO, Omanoil Company; Peter Ford, CEO, Port of Salalah and 
				Nicholas Barakat, CEO, OCTAL. The invitees to the event include 
				over 600 business leaders and decision makers from Oman’s 
				corporate sector and the government.<br>
				<br>
				Towards the end of the Debate, Oman’s Top 20 companies in the 
				year of 2011, based on OER’s listing of the 20 largest companies 
				on the Muscat Securities Market, will be felicitated. The 
				listing has established itself as an authoritative way of 
				looking at companies in Oman over the last nine years.<br>
				<br>
				The strategic partners for Oman Debate 2011 are Ahlibank, Galfar 
				and Nawras while OCTAL is the associate partner. The support 
				partners are Al Habib, Port of Salalah, Sharq Sohar Steel and 
				Muriya Tourism Development. The media partners of the event are 
				Times of Oman and Al Shabiba newspapers while Mazoon Printing 
				will be the print partner and Infoline the official call centre.<br>
				<br>
				All the previous editions of Oman Debate witnessed a 
				no-holds-barred discussion as government representatives, 
				private sector leaders and the public talked about the most 
				pressing issues facing the country.</td>
