						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">
						Creating a green umbrella<br>
						</font><font face="Verdana" size="2">
                		<br>
						The third edition of Oman Green Awards honoured 
						outstanding environmental vision, endeavours and 
						achievements of corporates and individuals</font></b><p align="justify" style="margin: 3px 10px">&nbsp;<p align="justify" style="margin: 3px 10px">
						<font face="Verdana" size="1"><b>Green IT strategy</b><br>
						<br>
						Information Technology Authority has won the Green 
						Guardian Award for its Green Initiative under the vision 
						of responsible computing towards a sustainable 
						environment<br>
						<br>
						The Green Initiative of the Information Technology 
						Authority aims to promote Green IT by adopting 
						industry’s best practices to create a sustainable green 
						IT eco-system within the government and the community. 
						This will help to decrease pollution, encourage 
						recycling and build an environmentally friendly 
						computing society and working space.<br>
&nbsp;</font><p align="center" style="margin: 3px 10px">
						<img border="0" src="images/oga-july-12-12.jpg" width="400" height="362"><p align="justify" style="margin: 3px 10px">
						<font face="Verdana" size="1"><br>
						A green IT strategic plan was established to identify 
						and stamp ITA’s projects ‘Green’ after evaluating their 
						green practices and recognising them in public. Two 
						projects were certified Green2012 during Comex 2012. The 
						organisation also procured environmentally safe products 
						and equipment like energy-saving devices and also 
						encouraged recycling and adoption of responsible IT 
						practices which used less paper. Some of the initiatives 
						were exchange of electronic documents instead of printed 
						copies and raising employee requests to Admin and Human 
						Resource office and settling them electronically. ITA 
						was able to incorporate best practices of Green IT 
						industry into the health, safety and environment policy 
						of the ITA and closely monitors its implementation. The 
						organisation encouraged the use of environmentally safe 
						products and equipment like energy-saving devices and 
						eco-friendly products and lighting and replaced plastic 
						disposables with eco-friendly items. ITA was able to 
						recycle 400 kgs of waste paper. Other bests practices 
						included procuring energy-efficient products; creating 
						awareness and motivating clients to implement 
						‘virtualisation’ techniques to optimise the use of 
						server resources and energy-management system for the 
						ITA Data Centre operations.<br>
						<br>
						<b>Warding off eco hazards</b><br>
						<br>
						Oman Drydock Company (ODC), winner of the special 
						commendation in Green Guardian Award, established unique 
						environmental facilities like NIX recycling plant, an 
						incinerator and slop and sludge facility<br>
&nbsp;</font><p align="center" style="margin: 3px 10px">
						<img border="0" src="images/oga-july-12-13.jpg" width="400" height="266"><p align="justify" style="margin: 3px 10px">
						<font face="Verdana" size="1"><br>
						Oman Drydock Company (ODC) is the first ship repair yard 
						in Oman and has a large number of ships docking for 
						repairs in which a number of toxic chemicals are used 
						and left as waste. Through the environmental facilities, 
						the main goal of the establishment of these facilities 
						was to treat the waste resulted from the company’s 
						activities in a way by which the harm to environment is 
						eliminated or reduced to its minimum. For example, NIX 
						is a form of copper slag used extensively to remove the 
						old paint and rust from the hull of the ships during the 
						repairs. During this time, large amounts of waste NIX is 
						generated and this requires exceptional treatment and 
						disposal methods to prevent any harm to the environment. 
						By installing the NIX recycling plant, incinerator and 
						slop and sludge facility, the NIX is reused. Also, the 
						oil treated from the slop and sludge facility is reused. 
						While in terms of the incinerator, the garbage which 
						might end up in the landfill is combusted and the heat 
						generated is reutilised. The success could be easily 
						measured by the reduction of the waste which could end 
						up totally stored (or disposed of, depending on the 
						waste properties) without treatment. In terms of NIX 
						recycling plant, the treated amount of NIX is reused. 
						Also, the oil treated from the slop and sludge facility 
						is reused. While in terms of the incinerator, the 
						garbage which might end up in the landfill is combusted 
						and the heat generated is utilised.<br>
						<br>
						<b>Saving ecosystem</b><br>
						<br>
						The Marine Emergency Response Services of the Port of 
						Salalah, which walked away with the Green Habitat Award, 
						has been doing the important job of regulating leaking 
						containers and dangerous cargo that have harmful 
						consequence on the immediate ecosystems, sea, land and 
						air<br>
&nbsp;</font><p align="center" style="margin: 3px 10px">
						<img border="0" src="images/oga-july-12-14.jpg" width="400" height="266"><p align="justify" style="margin: 3px 10px">
						<font face="Verdana" size="1"><br>
						The Port of Salalah, which was among the winners of Oman 
						Green Awards in 2011 for its initiative to switch off 
						machinery during breaks, has once again come up with 
						another innovative measure to prevent environmental 
						catastrophes during port operations. Known as the Marine 
						Emergency Response Services, this initiative is meant to 
						regulate leaking containers and dangerous cargo that 
						have harmful consequence on the immediate ecosystems 
						which include sea, land and air. The goal of Marine 
						Emergency Response Services is to protect land, sea and 
						air ecosystems at the port and quicken the response 
						procedure for hazardous material spills and leaks from 
						the large vessels that dock at the Port of Salalah. This 
						initiative has come about in reaction to an incident 
						which happened in January 2010 and served as a real 
						wake-up call to the employees and customers of the Port 
						of Salalah. On that day, 22 tonnes of fuel leaked from a 
						ship, onto the sideway of a busy terminal berth and 
						consequently into the sea. Consequently, it resulted in 
						a five-month cleanup of the sea and sideways. One of the 
						major action taken by the Marine Emergency Response 
						Services was the construction of a tank large enough to 
						transport a 20-foot container if it leaks with hazardous 
						material. The tank was 100 per cent designed and built 
						at Port of Salalah by the port’s engineers and safety 
						specialists to handle dangerous material. The port also 
						successfully achieved in 2011 a “no spills and no 
						damage” year thanks to improvements in the reporting and 
						response time required to safely handle dangerous cargo.<br>
						<br>
						<b>Committed to energy conservation</b><br>
						<br>
						W S Atkins won the special commendation in Green Habitat 
						Award for its innovative methods and strategies to bring 
						about a behavioural changes among the employees<br>
						<br>
						With a unique project titled RACE, an acronym for 
						Raising Awareness Cutting Energy, the Oman office of W S 
						Atkins has demonstrated that awareness can be raised in 
						any office environment.<br>
&nbsp;</font><p align="center" style="margin: 3px 10px">
						<img border="0" src="images/oga-july-12-15.jpg" width="400" height="207"><p align="justify" style="margin: 3px 10px">
						<font face="Verdana" size="1"><br>
						The idea was to encourage Atkins staff to be conscious 
						of the amount of energy they are using and reduce it 
						wherever possible, helping to ensure they maintained 
						their competitive edge in the new low-carbon economy. 
						The programme provided the ‘how’ to both senior 
						management and employees to bring about a behavioural 
						change in themselves. W S Atkin’s office located in 
						Hatat house block B second floor joined the global 
						Atkins competition to reduce power consumption by a 
						simple method of pasting RACE stickers – Save energy: 
						Switch of lights – in all relevant places. Because this 
						was a competition, all users were actively involved in 
						reducing energy consumption. And last year the Oman 
						office won a gold medal. The company employees were able 
						to come up with innovative methods and strategies used 
						to implement the Raising Awareness Cutting Energy 
						process. Employees also saw the importance of turning 
						the computer monitors off during lunch break and at the 
						end of the day while also learning the need to unplug 
						mobile phone/laptop chargers when not in use.<br>
						<br>
						Another significant achievement was that by turning the 
						airconditioning control up by 1° C and increasing room 
						temperature by 1°C, the organisation could reduce power 
						bills by up to 10 per cent.<br>
						<br>
						<b>A novel solution</b><br>
						<br>
						Petroleum Development Oman and BAUER Nimr were the joint 
						winners of Green Innovation Award for the Nimr Water 
						Treatment plant<br>
						<br>
						PDO’s Nimr Water treatment plant is a sustainable 
						project providing an environmentally friendly, 
						energy-efficient and innovative solution to the produced 
						water disposal challenge facing the oil industry. The 
						use of reed beds in a harsh desert environment has 
						proved to be ground-breaking. Several years of pilot 
						trials were needed before the project could finally come 
						on stream.<br>
						<br>
						The plantation concept has specially adapted waterlogged 
						plants with the ability to use atmospheric oxygen in 
						order to create both aerobic and anaerobic conditions in 
						their roots. The source of food for these waterlogged 
						plants is normally pollutants which include a wide range 
						of organic compounds like hydrocarbons through a process 
						of bio-degradation and filtration.<br>
						Produced water from the NIMR Production Station (NRPS) 
						with an average salinity of about 5,000 parts per 
						million (ppm) and average oil of 150 ppm in water is 
						treated and the resulting effluent water concentration 
						at the evaporation pond is less than two ppm oil in 
						water and about 15,000–20,000 ppm salt concentration.<br>
						<br>
						The planting of reeds has turned green a desert area of 
						2.4 million m2. This visible example can inspire local, 
						regional and international players to adopt or refine 
						similar solutions to their own water challenges. The 
						initiative has also promoted additional research to see 
						how the reeds can be used for bio-fuel and additional 
						water treatment for irrigation purposes. PDO is 
						supporting a joint team of Omani and Dutch scholars from 
						Sultan Qaboos University and Utrecht Universities who 
						are currently studying technologies to distil the saline 
						water for greenhouse irrigation use.<br>
						<br>
						<b>A technology export</b><br>
						<br>
						Berger Paints which developed the first low VOC lead 
						free paint walked away with special commendation in 
						Green Innovation Award<br>
						<br>
						Berger Paints Oman set its sights on using in-house 
						technology to develop a true next generation 
						Eco-friendly paint for the Omani market, thereby 
						creating awareness about eco-friendly paints in Oman. 
						The objectives was to develop a product by removing 
						volatile organic compounds, hazardous air pollutants, 
						alkylphenyl ethoxylates(APEO)as per GS 11 and to make it 
						LEED-compliant. The paint needed to be free from genetic 
						poison and heavy metals like chromium, arsenic, lead, 
						mercury. Finally, the R&amp;D department had to remove 
						crystalline silica from the paint, as the long term 
						exposure to silica increases chances of silicosis.<br>
						<br>
						The product named Royale Matt Emulsion, which was 
						developed over a three year period was successfully 
						launched last year and has captured almost 15 per cent 
						marketshare. It has got an external VOC compliance 
						certification and the audit of suppliers has proven that 
						the formulation is compliant with stated development 
						goals. The product has been adopted by sister units in 
						other countries all over the MENA region and Berger 
						Paints-Oman created a technology export from Oman.<br>
						Construction firms looking for LEED-compliant products 
						in Oman can now rely on a local supplier with the 
						technology to provide them with cost-effective 
						solutions. The paint will lead to a reduction in health 
						and environmental pollutants within the construction 
						sector in Oman. Royale Matt Emulsion has generated a 
						self sustaining trend towards eco-friendly paints in 
						Oman. Today, almost all players are looking for similar 
						products by either using imported technology or 
						home-grown solutions. As a follow up, Berger Paints is 
						itself reformulating the entire architectural range 
						along these lines. The company is also planning a much 
						larger R&amp;D centre in its upcoming plant in Sohar.<br>
						<br>
						<b>Play on<br>
						</b><br>
						Green Cover which won the Green Landscape Award has 
						brought natural infill football courses to Oman<br>
						<br>
						Green Cover has fixed a FIFA standard football course 
						with artificial grass and natural infill at the Al Mazra 
						Sports Club in Barka. The eco-friendly field is made up 
						using 100 per cent natural organic material instead of 
						using chemical infill. The company used FIFA approved 
						technology and eco friendly materials for laying the 
						course. Natural infill has been recommended by FIFA as a 
						standard court material. The natural infill is 
						environmentally friendly and scores over other infills 
						such as recycled black tyre materials.<br>
						<br>
						The advantage of natural infill is that there are no gas 
						emissions and it does not have the smell of tires which 
						comes from chemical infill. It reduces surface 
						temperature by 25 per cent and is recyclable. Such 
						courses are lead-free and are made up of non-hazardous 
						material. They are also less abrasive and this reduces 
						the chances of injury to players. Courses dilled with 
						natural infills drain quickly and are highly permeable. 
						They resist heat absorption but retain humidity, 
						enabling the field to stay cooler, reducing surface 
						temperature compared to other infills, allowing maximum 
						athletic performance with superior player stability.<br>
						<br>
						Courses filled with natural infills are sustainable over 
						a long period as they have an overall life span of 15 to 
						20 years. The company provides a full warranty for this 
						period ensuring good maintenance.<br>
						<br>
						<b>Constant innovation</b><br>
						<br>
						Commitment to conservation enabled Muscat Golf Course 
						Project to win special commendation in Green Landscape 
						Award<br>
						<br>
						Oman’s first 18-hole Green Golf course strives to reduce 
						the consumption of energy by using alternative sources 
						of energy like solar lights and using electricity to run 
						its golf carts. The watering of the course is done only 
						with desalinated and sewage water. The course is 
						planning to start drilling a new well to find natural 
						water. A key objective of Muscat Golf Course is to work 
						closely with experts who advise it on environment impact 
						and to find a balance to preserve course conditioning 
						throughout the year. The course is constantly devising 
						new methods and practices to meet the harsh dry 
						conditions especially through the hot summer months 
						without affecting the surrounding natural topography of 
						the golf course. The success of the first green golf 
						course in Oman is inspiring other companies to build 
						green golf courses.<br>
						<br>
						Muscat Hills is a unique integrated resort, comprising 
						an 18-hole PGA-certified green golf course and a high 
						quality residential development on the outskirts of 
						Muscat. The course strives to develop the game of golf 
						in the Sultanate and the youth of tomorrow through 
						junior programmes. The second phase of the project is 
						expected to begin in 2012. This will include the 
						construction of a five-star hotel managed by the 
						InterContinental Group of Hotels, a permanent clubhouse 
						and new onsite properties. The second phase is expected 
						to be completed in about two years’ time. Muscat Hills 
						Golf and Country Club currently has 240 members. The 215 
						residential units have been sold out and Muscat Hills 
						has emerged as a preferred place to live.<br>
						<br>
						<b>A productive solution<br>
						</b><br>
						Sultan Qaboos University’s Jatropha Plant, winner of 
						Green Research Award, can produce bio fuels and revive 
						the agricultural prospects of unproductive areas<br>
						<br>
						As a part of the research, a rocky and saline wasteland 
						was selected and Jatropha plants were planted and 
						irrigated with saline and treated wastewater. The 
						quality of soil improved as a result of the experiment 
						and the land could be used for the same or other crops. 
						The main objective of the whole project was to improve 
						the productivity of waste lands (unsuitable for 
						agriculture) and to maximise the usage of treated 
						wastewater. Apart from this the effort was to grow 
						economical (bio-fuel) plants, reduce the usage of 
						chemical fertilisers and to improve soil quality by 
						using natural resources. The project was also a 
						preliminary research to assess the possibilities of 
						Jatropha as a provider of bio-fuel.<br>
						<br>
						This technique offers numerous advantages like turning 
						barren soils into green lands. As the Jatropha plant is 
						not eaten by animals, it can work as a good fence for 
						protecting domestic plants. Farmers can harvest the seed 
						of the plant and get bio-fuel from it. The husk of the 
						seeds can be used as a fertiliser for edible crops and 
						the plant absorbs high amounts of carbon dioxide helping 
						the environment. In the long run rapidly disappearing 
						saline lands with almost seawater quality irrigation 
						water can be revived by using this research. Apart from 
						this, changed land use that is resulting in the 
						appearance of residential and commercial buildings on 
						the agricultural lands can be stopped. Thus, a great 
						loss to the natural land resources of the country can be 
						at least be minimised if not stopped totally. These 
						lands can be brought under agriculture again but with 
						newly changed economically attractive plants. As a 
						result farmers/owners of such lands can be re-employed 
						on their original lands once again. Since this plant can 
						be grown in Omani conditions, it can also be grown in 
						other Gulf countries.<br>
						<br>
						<b>Profiting from waste</b><br>
						<br>
						RAY International won special commendation in Green 
						Research Awards for introducing the first of its kind 
						technology to trun waste into harmless by-product<br>
						<br>
						Ray International’s oil sludge treatment plant at Qarn 
						Alam processes a hazardous waste known as oil sludge. 
						Oil sludge is a by-product of the petroleum industry 
						that is continuously generated during operations, 
						causing environmental problems. The project effectively 
						treats the sludge contained in the pits of Petroleum 
						Development Oman (PDO). The company has cleaned more 
						than 20,000 barrels of sludge. It has also recovered oil 
						and cleaned the soil and water by using a new 
						technology.<br>
						<br>
						The aim of the oil sludge treatment initiative is to 
						treat hazardous waste and change it into a harmless 
						by-products. The company’s goal is to process all the 
						eight sludge pits in the hazardous waste area of Qarn 
						Alam (more than 80,000 barrels of hazardous waste).<br>
						<br>
						This is a new technology that has been successfully 
						implemented commercially only in the Sultanate. The 
						initiative has received the Governor’s Award for 2011 
						project of the year (third place) and has proven to 
						remove a great environmental liability for the operator. 
						This is the first time that a project like this has been 
						implemented around the world. Ray International has 
						recovered valuable crude oil for PDO and returned clean 
						water and sand to the environment. Emboldened by this 
						success the project is now being implemented in other 
						collection sites of PDO like Mukhaizna and Fahoud. It 
						has also been presented to other companies like OXY and 
						Daleel for their respective sites.<br>
