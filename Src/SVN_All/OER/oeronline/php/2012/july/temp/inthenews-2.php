						million metric tonnes per year<br>
&nbsp;</font></b><p align="center" style="margin: 3px 10px">
						<img border="0" src="images/inthenews-apr-12-3.jpg" width="500" height="333"><p align="justify" style="margin: 3px 10px">
				<font face="Verdana" size="1">
				<br>
				Vale achieved full production capacity of 9 million metric 
				tonnes per year at its $1.36bn industrial complex in the Port of 
				Sohar. By creating a ‘virtual iron ore mine’, the global 
				diversified mining company’s operations in the Sultanate will 
				contribute to solidifying the country’s frontier market status 
				and competitiveness in the global economy as a base for 
				integrated steel production in the Middle East, North Africa and 
				Asia. The company’s operations in Oman employ 1,200 people of 
				which 450 are Vale’s direct employees and 750 are contractors 
				working on site, with another 3,120 jobs generated indirectly. 
				The company has achieved an Omanisation rate of more than 60 per 
				cent amongst its direct employees.<br>
				<br>
				Vale’s industrial complex comprises two pelletizing units, each 
				with a nominal production capacity of 4.5 million metric tonnes 
				of direct-reduction pellets per year, and a distribution centre 
				with a throughput capacity of 40 million metric tons per year. 
				The company’s first production line commenced operations in 
				April 2011, successfully supplying pellets to key steel 
				producers in the region. To maximise its distribution centre 
				capabilities, Vale partnered with Sohar Industrial Port Company 
				to build a 1.4 kilometre deep-water terminal. This terminal is 
				one of the first ports in the world to receive very large ore 
				carriers (VLOCs) that boast a capacity of 400,000 tons and are 
				responsible for transporting the world’s purest iron ore from 
				Brazil to the shores of the Sultanate.<br>
				<br>
				The pelletizing plant and distribution centre in Oman, together 
				with a floating transfer station in Subic Bay in the 
				Philippines, a distribution centre and port under construction 
				in Malaysia and the VLOCs, are part of Vale’s strategy to 
				increase its flexibility and competitiveness in serving the 
				world’s fastest growing markets, on an equal footing with its 
				closest competitors.<br>
				“We have a solid commitment to Oman,” says Vale president &amp; CEO, 
				Murilo Ferreira. “We have joined forces with the government, the 
				private sector and the community where we operate to catalyse 
				sustainable development. At each stage, we ensure that our 
				relationships with our partners are based on transparency, 
				respect and trust.”<br>
				<br>
				Vale has invested $40mn in technologies to reduce the project’s 
				environmental impact, such as electrostatic precipitators and a 
				3,150 metre wind fence to control particulate matter emissions. 
				The site also has a continuous emissions monitoring system and 
				100 per cent of the water used is recycled. To augment its 
				highly efficient logistics system, Vale has signed an agreement 
				with Oman Shipping Company to build four chartered ships 
				exclusively for the company’s use. Two vessels are scheduled to 
				become operational by mid 2012 while the other two by the end of 
				2012. <br>
				<br>
				“Over the last 41 years, Oman has established itself as a unique 
				development model amongst its neighbours,” says Marcos Beluco, 
				Vale’s country manager. “His Majesty Sultan Qaboos bin Said’s 
				Vision 2020 is based on self-sustained growth in a private 
				sector-led and export-oriented economy. In line with this 
				vision, we have created a virtual iron ore mine in the Sultanate 
				that will support its steel intensive phase of economic 
				development and serve the growing demand in the region.” Beluco 
				explained that Vale was dedicated to realising the nation’s 
				efforts to significantly reduce its dependence on oil by 
				diversifying economic resources.</font></td>
