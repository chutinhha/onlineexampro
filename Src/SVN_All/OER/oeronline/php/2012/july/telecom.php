				marking a historical milestone with the launch of its 
				state-of-the-art 4G LTE network that is centered on providing 
				unparalleled fast broadband speeds in the coming few days. The 
				launch of Oman’s first 4G LTE network shows the company’s 
				leadership and commitment in providing the most advanced 
				broadband services.<br>
				<br>
				Long Term Evolution 4G will be the prevailing future mobile 
				broadband technology and is capable of delivering speeds upto 
				100 Mbps. This technology will provide customers the opportunity 
				to stream, download, upload data and play online games faster 
				than ever before. <br>
				“When developing our 4G LTE proposition, we have focused on real 
				customer broadband needs which were all about speed, reliability 
				and quality. The new low-latency network will offer superior 
				speeds which we expect will have a strong appeal to individual 
				and corporate customers alike making them the perfect fit for 
				almost every customer’s needs,” Haitham Abdullah Al Kharusi, 
				vice president of Omantel’s consumer business unit said. <br>
				<br>
				“We have been investing in adopting new technologies focusing 
				always on the matured technologies that will enhance our 
				existing offerings for our customers to benefit from. With the 
				awarding of the 4G LTE network contracts last month by Tender 
				Board, we have now reached a stage where our customers can soon 
				start enjoying 4G LTE services through affordable modems and as 
				the technology is developing we will see more devices and 
				handsets with even lower prices.” Haitham Al Kharusi further 
				explained. <br>
				<br>
				<img border="0" src="images/telecom-july-12-1.jpg" width="150" height="178" align="right" hspace="8" vspace="3">“As 
				we strive to make our customers the first in the Sultanate to 
				experience this state-of-the-art network, we are proud to 
				announce that we will launch the Sultanate’s first 4G LTE 
				network soon in different areas where SQU Campus will be one of 
				the first areas allowing thousands of students, academic faculty 
				and employees as well as general public to subscribe and truly 
				enjoy 4G LTE services for the first time.” Al Kharusi added. <br>
				<br>
				Commenting on Omantel’s decision to launch 4G LTE services, Prof 
