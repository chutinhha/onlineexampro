						<p align="justify" style="margin: 3px 10px">
						<strong><span class="style27">CELEBRATION OF SUCCESS</span><span class="style14"><br>
&nbsp;</span></strong><p align="justify" class="style13">
						<b>AIWA Awards for Best Performing Companies felicitated 
						the top ranked companies listed on Muscat Securities 
						Market</b><br>
						<br>
						<br>
						In a red carpet gala function at Shangri La Barr Al 
						Jissah on June 10, AIWA Awards For Best Performing 
						Companies recognised and celebrated the success of top 
						achievers of corporate Oman in 2011. HE Dr Rasheed bin 
						Al Safi Al Huraibi, chairman of the Tender Board, was 
						the chief guest for the award function. The event was 
						held under the auspices of HH Sayyid Tarik bin Shabib. 
						AIWA Awards for Best Performing Companies are instituted 
						by Alam al-Iktisaad Wal A’mal (AIWA), Oman’s leading 
						Arabic monthly business magazine, published by United 
						Media Services (UMS).<br>
						<br>
						“A roaring success, AIWA Awards for Best Performing 
						Companies has emerged as a benchmark for measuring 
						excellence in corporate performance in Oman,” commented 
						Alpana Roy, executive vice president, United Media 
						Services (UMS). The banks and non-banking finance 
						companies (NBFCs) swept the AIWA Awards For Best 
						Performing Companies. In the large cap segment, AhliBank 
						topped the list of winners for the second successive 
						year in a row. It was followed by four other winners 
						including National Bank of Oman, BankSohar, BankMuscat 
						and Omantel.In the mid cap segment, Oman Refreshment 
						emerged on top. The other winners in the segment 
						included United Finance, Taageer Finance, Oman Chlorine 
						and Al Omaniya Financial Services. In the small cap 
						category, Oman Fiber Optics was ranked number one. It 
						was followed by other winners including Muscat National 
						Holding, Sahara Hospitality, Oman Orix Leasing and 
						National Gas. <br>
&nbsp;<p align="center" class="style13">
						<img border="0" src="images/event-july-12-1.jpg" width="500" height="280"><p align="justify" class="style13">
						<br>
						His Excellency Mohammad al Zubair, the founder of Zubair 
						Group and advisor to His Majesty was honoured with the 
						AIWA Lifetime Achievement Award. His son Husam Al 
						Zubair, who is the vice chairman of Zubair Automotive 
						Group, received the trophy on behalf of HE Mohammad al 
						Zubair. Petroleum Development Oman (PDO) won the AIWA 
						Award for Excellence in Corporate Leadership. Noted 
						businessman Hussain Salman Al Lawati, vice chairman and 
						managing director of Oman Cables Industry and chairman 
						of Oman Aluminium Processing Industries, walked away 
						with the AIWA Global Omani of the Year Award. <br>
						<br>
						Leading brands in the Sultanate had joined hands with 
						Alam al-Iktisaad Wal A’mal, the leading business 
						publication from United Media Services, in bringing the 
						AIWA Awards for Best Performing Companies to life. Bank 
						Sohar and Genesis Prada (the luxury car series from 
						Hyundai) were the Strategic Partners. Mouawad, Muscat 
						Pharmacy and The Wave, Muscat were the support partners. 
						Gulf Baader Capital Markets (GBCM) was the knowledge 
						partner whereas KPMG was the validation partner for the 
						ranking process used for the awards. Times of Oman and 
						Al Shabiba were the media partners.<br>
