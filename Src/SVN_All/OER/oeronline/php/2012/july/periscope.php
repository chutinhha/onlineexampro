						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">
						Downside risk for global markets<br>
						</font><font face="Verdana" size="2">
                		<br>
						GCC stock, bond and property markets cannot escape 
						collateral damage from the European crisis and a Wall 
						Street stock market crash<br>
						<br>
						</font></b>
						<i><font color="#333333" face="Verdana" size="1">BY Matein Khalid</font></i><b><font face="Verdana" size="2"><br>
                		</font></b><font face="Verdana" size="1">
				<br>
				Bear markets, like Count Tolstoy’s unhappy families, are all 
				different. The summer of 2012 is unlike the post Lehman swan 
				dive of 2008, the technology bubble bust of 2000-1 or the Asian 
				currency meltdown of 1998. However, like its predecessors, the 
				European debt crisis can well escalate into global financial 
				contagion and a worldwide banking credit crunch. It is 
				impossible to estimate the value of the S&amp;P500 index in such an 
				uncertain scenario but American equities could well fall 25 per 
				cent below their April highs before the summer is over.<br>
				<br>
				One, the softness in the last two monthly nonfarm payroll 
				numbers demonstrates that recession risk in America is all too 
				real, despite the improvement in home sales/price indices and 
				the steep fall in oil/gasoline prices on the eve of the driving 
				season. The Bernanke Fed could yet expand its balance sheet, 
				even implement QE3, though the dramatic fall in US Treasury bond 
				yields eases the pressure for additional monetary policy 
				stimulus. The 20 per cent drop in both West Texas and Brent 
				crude oil prices is a steroid shot for the US consumer. Net-net, 
				the US could muddle through 2012, albeit with mediocre 2 per 
				cent GDP growth.<br>
				Two, the collapse in oil and copper signals a hard landing in 
				the Middle Kingdom, the world’s great gas guzzling sweatshop 
				economic colossus. PMI, industrial production, bank loan growth, 
				import, exports have all plummeted and there is every reason now 
				to doubt if Premier Wen’s 7.5 per cent GDP growth target is 
				realistic. This is the reason the Chinese Politburo has swiftly 
				responded with three PBOC successive reserve ratio cuts and a 
				lending rate cut since November. China will do everything 
				possible to ensure that economic growth does not collapse as the 
				Communist Party anoints a new generation of leaders at the Party 
				Congress to succeed Hu Jintao and Wen Jiabao.<br>
				<br>
				<b>Endgame of European crisis<br>
				</b>Yet the real bogeyman for the financial markets is neither 
				the US nor China but the endgame of the sovereign debt crisis in 
				Europe. Spain, with its crippled banking system, a Madrid 
				government that has lost credibility with its fiscal flip flops 
				and inability to control spending by regional governments in 
				Catalonia and Andalusia (the post Franco political devolution 
				settlement in Spain makes the 17 regional capitals autonomous in 
				their budget processes), its 24 per cent unemployment rate, its 
				two trillion Euro public debt, is a systemic threat, despite the 
				recent EU bank lifeline. What will be the impact on Spain if 
				contagion spreads from a Greek EMU exit across the banking 
				systems of Club Med? This is Europe’s ‘Lehman risk’ since 
				Hellenic contagion guarantees a severe European recession and 
				systemic shocks across the international banking system and 
				credit crises. At that fateful moment, the Fed, the ECB, the 
				PBOC, the Bank of Japan and the Bank of England could be forced 
				to engineer the mother of all “shock and awe” monetary 
				reflation.<br>
				<br>
				However, the German diktat on austerity and refusal to sanction 
				ECB firefalls for European banks means the risk of Drachmageddon 
				and Lehman Part Two in the credit markets is almost certain if 
				Alexis Tspiras of the radical left Syriza party wins the Greek 
				elections. If Tspiras repudiates the Berlin/IMF bailout terms, 
				all bets are off. Greek banks lose their ECB lifeline, the 
				Athens government runs out of cash, its citizens could well run 
				out of food, fuel and medicine. The Athens government would 
				fall, new elections or a military takeover would be the endgame 
				of the economic shock (Greece was ruled by a military junta in 
				1967-73). The lamps will flicker and die across the Old World, 
				exactly as they did in August 1914.<br>
				<br>
				<b>Brink of catastrophic destruction<br>
				</b>Europe is now in deep crisis and the European Commission 
				will lean on the Germans to accede to a “lender of the last 
				resort” role for the ECB, a common deposit insurance scheme and 
				the issue of Eurobonds. After all, even Chancellor Merkel has 
				now conceded that “there are no taboos” in her quest for mehr 
				Europe (more integration). As Bretton Woods, the Plaza/Louvre 
				Accords, the post Lehman credit crunch in 2008 remind us, the 
				world’s most powerful governments and central banks will not 
				hesitate to act to prevent a catastrophic destruction of the 
				international monetary system. The EMU will not unravel, even if 
				Greece exits amid short term monetary shocks.<br>
				<br>
				The S&amp;P 500 index has closed below its 200 day moving average in 
				June and West Texas is below its $89 one year Fibonacci 
				retracement level, so I believe the trend in global equities and 
				commodities is still lower. A bear market is in full swing in 
				bellwether emerging markets such as India, Russia, China and 
				Brazil, whose currencies and stock markets are in free fall. 
				However, brief market panics (2006, 1998, 1987) are a different 
				animal than protracted bear markets accompanied by economic 
				recessions (2001, 2008-9).<br>
				<br>
				What is the downside risk for American equities if a Greek exit 
				escalates into contagion, a banking crisis in Europe, a hard 
				landing in China and recession in the US. S&amp;P500 index earnings 
				could then easily fall by 10 per cent. The stock market 
				valuation metrics will derate as EPS growth estimates are 
				revised lower and volatility rises (VIX in 40-45 range at the 
				peak of crisis, as in 2011). The markets would then expect $90 
				index earnings and derate to a multiple of 10. So downside risk 
				on American equities could then be catastrophic, a fall in the 
				S&amp;P500 index to 900.<br>
				<br>
				There is no way GCC stock, bond and property markets can escape 
				collateral damage from the European crisis and a Wall Street 
				stock market crash. Brent crude oil has fallen by $25 but, if 
				Europe and China growth plunges in unison, Brent could fall to 
				$60-70, exactly as happened in 2008. The GCC currency pegs mean 
				the global safe haven surge in the dollar imports deflation risk 
				in the regional stock and property markets, where additional 
				corrections are possible. Remember JP Morgan’s fateful warning: 
				“Liquidity is like a cab in New York on a rainy night. It 
				disappears just when you need it the most”.&#8194;<br>
