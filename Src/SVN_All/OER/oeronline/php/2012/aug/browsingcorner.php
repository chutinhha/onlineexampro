				<img border="0" src="images/browsingcorner-aug-12-1.jpg" align="right" style="border: 1px solid #C0C0C0" hspace="12" vspace="3" width="150" height="234"><b>Tim 
				Wu builds a convincing case for the communication industry to be 
				regulated by an ‘information morality’ in this seminal book. An 
				OER review</b><br>
				<br>
				The Master Switch: The Rise and Fall of Information Empires is a 
				fascinating, wide-ranging, and inspiring book about 
				communications policy and information industries. This comes as 
				no surprise as Wu is one of America’s great information policy 
				scholars and communicators, probably best known for coining the 
				term ‘Net Neutrality.’<br>
				In Master Switch, Wu gives us a glimpse into his vast and broad 
				knowledge of communications policy and its history, the 
				groundwork that gave rise to his ideas, and presents an 
				inspiring path to a better world of better networks (even as he 
				shows the risks of not taking such a path). He makes the 
				convincing case that the Net is different, that its stakes are 
				higher than any communications battle in memory, though the form 
				of the battle is a familiar one. Wu is an accomplished scholar 
				who can write about complex ideas in ways that are accessible to 
				all. And the ideas he’s covering are as important as any in our 
				ideological marketplace today.<br>
				Wu’s great strength is in the breadth of his scholarship and in 
				his ability to use humour, clear language, and innovative 
				arguments to connect diverse ideas. Thus in Master Switch, we 
				have a brilliant explanation and history of what Wu calls ‘the 
				Cycle,’ through which information industries rise, consolidate, 
				monopolise, capture governments, force out competitors, and, 
				eventually, fragment into something less grandiose, less 
				perfect, but more vibrant, open, and innovative.<br>
				<br>
				Connecting the dots<br>
				The author connects the industrial and informational monopolies 
				of AT&amp;T, the film trust, the exhibitors monopoly, the radio 
				monopoly, the fight over FM, the censorship of the Hays Code for 
				film-makers, the liberation of the Hayes Code for operating 
				modems, the dashed hopes for a diverse and vibrant cable TV 
				landscape, and, ultimately, the invention of the Internet.<br>
				The uniqueness of communications as an industry means that 
				regulation and markets fail more often around them, and that the 
				failures are worse. In response to this, Wu builds the case for 
				a set of principles around information industry ownership, 
				concentration, and structure, and proposes that these be 
				regulated largely by an ‘information morality’ -- not by a 
				single regulatory agency or a single statute book, but 
				ultimately by an emergent consensus about the value of 
				information freedom as a vital substrate for free speech and 
				free societies.<br>
				Wu wisely avoids magic-bullet solutions to the inevitable 
				problems of the communications industry. Simply keeping 
				government out of the business does not work, because the 
				industry tends to form private monopolies if left alone. And 
				having the government run the business, as England and other 
				countries have tried, presents its own problems. The government 
				itself is a monopolist and often behaves like one. Wu instead 
				calls for constraining “all power that derives from the control 
				of information.” He writes, “If we believe in liberty, it must 
				be freedom from both private and public coercion.” The central 
				idea of this book, is that market competition brings enormous 
				benefits, but the market itself does not ensure competition – 
				or, more broadly, desirable outcomes.&#8194;</font></p>
