						<td valign="top">
						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">
						Change the mindset<br>
						</font><font face="Verdana" size="2">
                		<br>
						“Oman like any other country needs to review its 
						education system in line with a clear vision as to what 
						skill sets would we need for the next 20 to 30 years. We 
						need to be able to compete globally and not just 
						locally,” says Malak Al Shaibani, General Manager – HR 
						Solutions, Talent2. She will take part as a panelist in 
						Oman Forum – Building National Talent. Excerpts of an 
						interview <br>
						&nbsp;</font></b><p align="center" style="margin: 3px 10px">
						<img border="0" src="images/hr-apr-12-1.jpg" hspace="8" vspace="5" width="300" height="450"><p align="justify" style="margin: 3px 10px">
						<b>
						<font face="Verdana" size="2">
						<br>
                &nbsp;</font></b><p align="justify" style="margin: 3px 10px">
						<font face="Verdana" size="1"><b>How important is the 
						school education in building national talent? Do you 
						think the current school education system is able to 
						achieve its purpose?</b><br>
						The education system of any country consists of school 
						or basic education and tertiary or vocational education 
						as an integrated building block for national talent. One 
						needs all these to prepare national talent in any 
						country.<br>
						<br>
						As for basic education or school that is the first 
						building block and like any country that competes 
						globally we need to review it in light of global 
						developments and requirements of new skills in the 
						economy. <br>
						<br>
						<b>What type of competence do we want a high school 
						graduate to have? </b><br>
						How are our kids performing in maths and science and in 
						reading and writing Arabic and English? If we look at 
						vocational or technical training or even university 
						education, are we meeting the requirements of the 
						market? Do we have the skill pool that will attract a 
						multinational corporation to set up in Oman and look at 
						Omani talent as one of the key competitive advantages? 
						Why is it that countries such as Singapore constantly 
						score high when it comes to students’ performance? Our 
						institutes of education at all levels need to set 
						benchmarks so that we could identify where we are, where 
						we want to be and what needs to be put in place now to 
						reach that goal.<br>
						<br>
						In my opinion Oman like any other country needs to 
						review its education system in line with a clear vision 
						as to what skill sets would we need for the next 20 to 
						30 years. We need to be able to compete globally and not 
						just locally.<br>
						<br>
						<b>What is the mindset of the corporate sector as far as 
						incorporating best HR development practices in the 
						overall business planning processes? What change you 
						have observed in their HR planning and development 
						practices over the years?</b><br>
						The mindset of the private sector with regard to best HR 
						practices has a long way to go. It starts initially with 
						understanding the role of human resources and its 
						strategic importance for any company. I am afraid many 
						companies in Oman still practice personnel management 
						which is basically a focus on the administrative tasks 
						or transactional tasks of human resources such as 
						attendance, payroll, leave management etc. In such 
						companies, the HR role is limited and carries very 
						little weight and it can therefore easily report to 
						finance or any admin function. What typically happens is 
						the individual appointed to run HR is not qualified. I 
						have seen companies appointing PRO with high school 
						diplomas to these positions merely to comply with the 
						labour law. In the process, they are being unfair to the 
						individual and sends a clear message what they think of 
						their human resources. Other companies have grown a bit 
						further and also have a training element attached but 
						still the understanding is that HR manages the people of 
						the organisation and resolves employee issues. <br>
						<br>
						Once again this is a limited role. There are only a few 
						companies who are now recognising that HR has to play a 
						strategic role and has to report to the CEO. These 
						companies are in a transition mode but tend to support 
						and introduce best practices and are advanced in some 
						areas but need development on others. <br>
						<br>
						However, best practices in HR is a journey and it takes 
						time and needs a champion CEO, able and willing to do 
						this. It is funny that leaders talk about human 
						resources as the most important element while their 
						actions or decisions are to the contrary! In a nutshell, 
						very rarely is HR configured in the planning process. If 
						it is, it is usually part of the budgeting process where 
						one needs to plan for extra heads or salary increases.
						<br>
						<br>
						<b>Do you think the inhouse HR divisions within the 
						private sector companies are competent enough to setup 
						and implement effective HR development and training 
						processes in place? What role can be played by 
						independent HR consultancy firms in making them achieve 
						this objective?</b><br>
						I think HR professionals are very few and far in between 
						but what I have noticed is that good companies who 
						understand the significance of HR are willing to seek 
						the best and put in place the best practices.<br>
						<br>
						I think more and more companies are realising that if 
						you get your human resources wrong you can still 
						function but it can come back and bite you and it will 
						cost you financially as well as spoil your reputation. 
						Therefore there is progress at least in understanding 
						something needs to be done. However, the issue is what, 
						when, by whom and for how long? There are companies in 
						the private sector specifically those in the oil and gas 
						sector and financial services who are at the forefront 
						of HR practices. However, one needs to be careful and 
						customise what can work in Oman. As I said it is a 
						journey. Consulting companies can help if they can 
						customise a solution for a particular company. There is 
						a danger when consultants come in to introduce the best.<br>
						<br>
						Either it cannot be implemented or there is no knowledge 
						transfer in the process. As a consultant, one has to 
						have the honesty and integrity to even turn down 
						business if it is not going to work. The danger comes 
						when too many HR changes are introduced at one go 
						causing instability and what I call ‘the flavour of the 
						month syndrome’. In such cases, consultants get their 
						money and management loses credibility. It is extremely 
						important that the best HR practices should have support 
						from the board level and the CEO. There is ‘buy in’ from 
						the key stakeholders in the company and more 
						importantly, they are introduced at the right time.<br>
						<br>
						Finally what is more crucial is that the HR best 
						practices have to be part and parcel of the that 
						organisation’s vision and objectives so improvement is 
						also measured in people’s performance and can be linked 
						to the bottom line; if that cannot be proven, no one 
						will do it. <br>
						<br>
