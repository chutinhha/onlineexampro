						<font face="Verdana" size="4" color="#C00000">
						Deep bonding</font></b><br>
				&nbsp;<p align="justify" class="style13" dir="ltr">
				<b>Eleven years with Aston Martin, including nine years in 
				diverse roles at its factory in UK, helped Elizabeth 
				Walsham-Gray, Brand Manager, Aston Martin Oman, hone her 
				management and leadership skills. Muhammed Nafie reports</b><p align="center" class="style13" dir="ltr">
				<br>
				<img border="0" src="images/beyondboardrooms-aug-12-1.jpg" hspace="13" vspace="3"><p align="justify" class="style13" dir="ltr">
				<br>
				It is not an overstatement to call Elizabeth Walsham-Gray a 
				member of the extended Aston Martin family. It’s not only 
				because her husband has worked with the same company for the 
				last 17 years as well; but because, having started her career 
				with Aston Martin’s factory in UK 11 years ago, Elizabeth has 
				scaled new heights in her professional and academic life with 
				the luxury sports car brand. Since the last year, as the brand 
				manager of Aston Martin Oman, she has been looking to create 
				more brand awareness about the cars in Oman. In between, she 
				also had a short stint with Aston Martin Qatar.<br>
				<br>
				During her nine years in the factory, Elizabeth gained extensive 
				experience having been tasked with different responsibilities. 
				She started with the materials, planning and logistics division; 
				later became a production supervisor on the shop floor; and then 
				worked as an integral part of the launch team looking after the 
				new models. In the meantime, she completed her Masters of 
				Science degree in Engineering Business Management, as part of 
				Aston Martin’s prestigious management training programme. “I had 
				a Bachelor’s (honours) Degree already, and the company 
				encouraged me to take my skills further forward by doing a 
				Master’s,” she says. “I could study alongside my work in the 
				factory, and I was very lucky that my final year dissertation 
				topic was centered around the role I was doing, meaning that it 
				was very easy to translate everything I studied back into the 
				work environment.” <br>
				<br>
				Nine years in the factory stood Elizabeth in good stead when it 
				comes to management and leadership skills. As a senior 
				production supervisor, she led a team of over 50 people. “I was 
				able to lead the team very effectively, mentoring, coaching and 
				teaching what I know about the brand and the business. I had a 
				team of five lead supervisors working for me. The important 
				thing was to recognise what individual skills they have and 
				encourage them,” she says. <br>
				<br>
				<b>New experience</b><br>
				The shift to Oman, a country which she loves very much, was a 
				completely new experience for her. “Business is very quiet here 
				in Oman,” she admits. “It’s a very different market here, 
				compared to Qatar where there is more willingness to spend money 
				on luxury cars and a lot of awareness about the brand. Here in 
				Oman people are not as aware of Aston Martin, as they are in 
				Qatar, Dubai or Abu Dhabi for example. It is very difficult to 
				come here with the brand I know very well to realise that other 
				people don’t recognise it as perhaps we think they should. In 
				other countries in the GCC, it is as popular and exclusive, if 
				not more than, as Ferrari and Bentley.”<br>
				<br>
				However, as Aston Martin is celebrating its 100th anniversary 
				next year, Elizabeth is working to generate more awareness about 
				the brand in the Sultanate. “I have taken on a new sales manager 
				who has been working in Oman for many years, and knows the 
				market very well. He comes with a wealth of experience, and is 
				helping to create more brand awareness.”<br>
				<br>
				Coming from a working ambience where one is used to rigid 
				starting and finishing times, Elizabeth found it difficult to 
				cope with a working environment where deadlines are not that 
				strictly adhered to. “In the factory I was always able to 
				maintain the strict balance between professional and personal 
				time, whereas here in the Middle East that balance gets a bit 
				blurred,” she adds. “However you have to make sure that you have 
				your time at the weekend and in the evening to separate yourself 
				from the busy schedule in office.”&#8194;<br>
