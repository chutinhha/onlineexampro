						<font face="Verdana" size="1">
						Rezidor, a member of the Carlson Rezidor Hotel Group, 
						has launched a group-wide energy-saving initiative, 
						‘Think Planet’ which targets an energy consumption 
						reduction of 25 per cent by the year 2016 in all its 
						hotels across Europe, the Middle East and Africa.<br>
						<br>
						Think Planet has been developed and is driven by a 
						specially formed, interdepartmental and cross-regional 
						team at Rezidor – with representatives from tchnical 
						services, operations, purchasing, finance, and 
						responsible business. The programme’s first phase, a 
						company-wide energy audit, has already been carried out 
						and resulted in a general energy action plan listing the 
						group’s top energy priorities.<br>
						<br>
						“‘Think Planet’ reinforces Rezidor’s position as one of 
						the industry leaders in terms of sustainability. Many of 
						our hotels have already introduced progressive, 
						environmentally friendly measures – now we are spreading 
						these initiatives into all our properties and focus on 
						key energy-saving investments that will have the biggest 
						impact”, said Inge Huijbrechts, director responsible 
						business at Rezidor. “At the same time, we are currently 
						faced with significantly rising energy costs in many 
						countries, along with government carbon taxation. Moving 
						forward, reducing our energy consumption will also 
						contribute to maximising our profits”, added Wolfgang M. 
						Neumann, executive vice president and chief operating 
						officer at Rezidor.<br>
						<br>
						<b>Smart energy</b><br>
						In hotel operations, Think Planet focuses on creating 
						smart energy habits – such as switching off lights and 
						equipment, turning heating and cooling to eco-mode, and 
						reporting energy related issues that request attention 
						and preventive maintenance. The basic ‘Think Planet’ 
						tools are simple and low-cost solutions: They comprise 
						daylight and motion sensors for lighting, condensation 
						water recuperation from air-conditioning, and alarms on 
						freezers. Considerable investments are made to 
						completely move to energy efficient lighting in all 
						hotels, install variable extraction systems in the 
						kitchens, and optimise building management systems and 
						heating-, ventilation- and air condition installations. 
						“We focus on energy saving investments with a return on 
						investment up to seven years”, explained Inge 
						Huijbrechts. The energy saving results are monitored and 
						verified through monthly reports on environmental key 
						performance indicators.<br>
						<br>
						A crucial factor of the energy-saving initiative is 
						staff engagement. In line with Rezidor’s unique service 
						philosophy Yes I Can! and with the help of the Think 
						Planet’s mascot, the cute firefly Lumi, all 35,000+ 
						employees participate in the initiative. Dedicated 
						audio- and video trainings inform about smart energy 
						saving habits in housekeeping, kitchen and meeting 
						areas, a monthly “Lumiquiz” and a competition for the 
						most efficient, energy saving hotel of the year ensure 
						continuous employee motivation and involvement.<br>
