						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">
						Ushering in a paradigm shift<br>
						</font><font face="Verdana" size="2">
                		<br>
						SIMPA Energy Consultancy and biz-group have partnered to 
						bring to Oman ‘Multipliers’, a novel management concept 
						drawn from Liz Wiseman and Greg McKeown’s global 
						bestseller. Delivered in a series of group workshops 
						with executive coaching and various assessment tools, 
						‘Multipliers’ teaches a set of behaviours that can 
						significantly increase a leader’s ability to identify, 
						nurture and grow the potential of others. A report by 
						Muhammed Nafie<br>
						<br>
						</font></b><font face="Verdana" size="1">
						<img border="0" src="images/management-oct-12-1.jpg" width="150" height="205" align="right" hspace="9" vspace="4">The 
						real test of a leader’s ability lies in leveraging the 
						full potential of the workforce. Across industries and 
						organisations, leadership is a critical force for 
						getting the most out of the team by extracting and 
						multiplying the intelligence of each individual. A good 
						leader is always expected to be a genius maker bringing 
						out the intelligence in others whereas a bad leader is 
						absorbed in his or her own intelligence by stifling 
						others. The former is bound to build collective, viral 
						intelligence in organisations, while the latter is 
						doomed to deplete the organisation of its crucial 
						intelligence and capability.<br>
						<br>
						But how can good leaders unleash the intelligence and 
						capability in others and harness them? Leadership expert 
						Liz Wiseman and management consultant Greg McKeown delve 
						deep into this question in ‘Multipliers: How the best 
						leaders make everyone smarter’. The book, published in 
						2010, describes good leaders as Multipliers because they 
						inspire employees to stretch themselves to deliver 
						results that surpass expectations; and calls bad leaders 
						‘diminishers’ because they are “idea killers, the energy 
						sappers and the diminishers of talent and commitment”.<br>
						<br>
						Drawing on two years of research in 35 companies spread 
						over four continents and interviews with more than 150 
						executives, Weisman has identified five disciplines that 
						distinguish Multipliers from their less enlightened 
						colleagues (the diminishers). They are talent, culture, 
						strategy, decision making, and execution. Multipliers 
						can do wonders for an organisation because they look for 
						talent everywhere and always acknowledge people not just 
						for the things they do exceptionally well but also for 
						things they do naturally, often without being asked and 
						sometimes without being paid. Multipliers also take the 
						time to understand the capabilities of each individual 
						so that they can connect employees with the right people 
						and the right opportunities – thereby building a 
						virtuous cycle of attraction, growth, and opportunity.<br>
						<br>
						The concept was brought to the Middle East by 
						Dubai-based biz-group, the first company outside of US 
						accredited to deliver ‘Multipliers’ workshops and 
						keynote speeches to c-level executives. Hazel Jackson, 
						CEO of biz-group, has recently been to Oman to deliver a 
						workshop on ‘Multipliers’ organised by SIMPA Energy 
						Consultancy for CEOs and senior level executives of the 
						leading companies here. The workshop has aroused a 
						ripple of excitement among the participants who believe 
						that the concept of Multipliers can have a resoundingly 
						positive and profitable effect on organisations.<br>
						<br>
						Hazel Jackson who got excited after reading the book 
						soon after it was published contacted Wiseman and got 
						the rights to introduce the concept in the Middle East, 
						Asia and Africa. She says this concept’s timing in Oman 
						couldn’t be better as the country is focused on multiple 
						initiatives for building national talent and identifying 
						talent gaps. “After reading the book I just adapted the 
						concept which is relevant to this part of the world. The 
						concept will help you to get twice as much capability 
						out of the people in your team. It helps with 
						Omanisation and it helps business leaders solve 
						difficult problems. It is about asking people to do hard 
						things; it does not put forward a soft, warm and cuddly 
						leadership style. It is getting people to solve tough 
						business challenges. As a leader you cannot solve tough 
						challenges alone, but you got to use the collective 
						intelligence capability of the people in your team.”<br>
						<br>
						Khalifa Al Mukheini, managing director of SIMPA Energy - 
						an Oman and UAE based energy consultancy that provides 
						advisory, recruitment and training services - also 
						believes that the concept holds immense potential for 
						Oman as the country invests heavily on building and 
						developing its indigenous talent to grow them as leaders 
						of tomorrow. “When I came across the concept of 
						‘Multipliers’ I was quite impressed. I thought this was 
						what we were actually missing in Oman for us as we 
						usually focus on technical and commercial skills 
						training and this was something that we need for our 
						clients in Oman. We started talking about introducing it 
						here. We then selected a very exclusive number of people 
						and companies in Oman for the first introductory 
						session. The response was extremely positive.”<br>
						<br>
						Elaborating on how ‘Multipliers’ can do wonder for Oman 
						and the region as a whole Hazel said, “A lot of our 
						research in the region has found that there are a lot of 
						people busy but secretly bored in companies and 
						‘Multipliers’ is a leadership and business technique 
						which teaches leaders how to get more out of those 
						people and how they get the right thinking. We need to 
						engage people more by asking them to do hard things. We 
						spend a lot of money hiring smart people but what 
						happens if you do not ask them to engage their brains?”<br>
						<br>
						Khalifa further believes that ‘Multipliers’ can serve as 
						a powerful tool for growing effective Omanisation 
						programmes and help local organisations in a number of 
						localisation programmes. “It is very timely for Oman. It 
						can help build competencies as employees feel more 
						empowered in organisations to do better and at some 
						point they may want to become entrepreneurs themselves 
						by starting their own organisations and creating 
						in-country value,” he said.<br>
						<br>
						Indeed, leaders who can identify the real competencies 
						of their workforce and tap into their capability, 
						creativity, talent, initiative, and resourcefulness 
						fully can usher in a paradigm shift within any scalable 
						firm’s corporate culture and bottom line, by utilising 
						the latent talent.&#8194;</font></td>
