						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">Italian 
						automotive luxury checks-in to Oman</font></b><p align="center" style="margin: 10px; ">
				<img border="0" src="images/automotive-oct-12-1.jpg" width="500" height="332"><p align="justify" style="margin: 10px; ">
				<font face="Verdana" size="1"><b>The luxury Italian marque of 
				Maserati Cars has come to Oman through a new state-of-the-art 
				showroom and after sales facility in Muscat. A report</b><br>
				<br>
				Maserati, the Italian luxury car brand, hopes to break new 
				ground in Oman with Alfardan Motors, the official importer of 
				Maserati automobiles into Oman commencing operations in the 
				country. Alfardan Motors’ latest venture in Muscat is expected 
				to be instrumental in the <br>
				‘re-birth’ of how these exceptional vehicles are brought to 
				market, as well as redefining customer care in the luxury 
				automobile segment.<br>
				<br>
				Customers can now look forward to a luxurious experience which 
				begins from the showroom itself, a state-of-the-art bespoke 
				facility which offers approximately 800 square metres on two 
				floors. The first floor can display up to eight cars 
				simultaneously, in addition to a reception area, a consultation 
				lounge, and the ‘configuration area’ where prospective customers 
				can select bespoke specifications for their purchase. The second 
				floor provides management offices and meeting rooms, which 
				together make up what has become now the manufacturer’s latest 
				showroom in the Middle East, designed with compliance to the 
				highest international standards and corporate identity designs 
				of Maserati.<br>
				<br>
				Alfardan Motors is a subsidiary of the Alfardan Automotive 
				Group, renowned for its leadership within the luxury automobile 
				sector. Based on the success it has enjoyed in its domestic 
				market of Qatar, Alfardan Automotive Group has been awarded the 
				status of official importer of Maserati to the Omani market. 
				Omar Alfardan, president of Alfardan Motors, says, “We are proud 
				of our long relationship with Maserati. We see Oman as a 
				promising market and we are pleased to extend our operations 
				with Maserati here. We feel confident to deliver on our promise 
				to provide the highest levels of customer care to Maserati 
				owners and enthusiasts, as well as provide the latest models 
				which customers can order to the most exclusive specifications.”<br>
				<br>
				Talking about Alfardan Automotive Group, Omar says over the 
				years, the group built an enviable reputation for excellence 
				within both sales and the after sales sectors. This is evidenced 
				by the awards it has received and, particularly relevant here is 
				the recognition for Alfardan Sports Motors in 2008, 2009, 2010 
				and 2011 by Maserati Regional Office announcing Qatar as the 
				‘Aftersales Importer of the Year’ for the Middle East and 
				Africa.<br>
				<br>
				“This is the kind of experience and expertise that Alfardan 
				Motors brings to the Omani market: a great customer service 
				through a dedicated and knowledgeable sales team, as well as our 
				award-winning after sales service. Our service is best defined 
				by our belief that any sale is only the beginning of what will 
				hopefully be a long and mutually beneficial relationship with 
				the customer,” Omar says.<br>
				<br>
				Umberto Cini, regional managing director of Maserati Middle East 
				and India, is extremely pleased to partner Alfardan Motors in 
				Oman while saying, “we are confident that Maserati is now part 
				of a team that will give the Omani market the attention it 
				deserves, its customers the service they expect ,and a partner 
				that will avail itself of the massive opportunity that this 
				market represents.”<br>
				<br>
				On the reasons why Maserati is confident that Alfardan Motors 
				will succeed in Oman, Umberto says, “This is because of the 
				relationship that already exists between Maserati and Alfardan 
				Sports Motors in Qatar. The partnership between these two 
				companies is a strong one, with a history of success and 
				achievement. We feel that these gains will also be seen in 
				Oman.”<br>
				<br>
				Umberto emphasises that Oman is a very important market for 
				Maserati. According to him, the Sultanate has shown a remarkable 
				growth potential in the luxury car sector and since Maserati 
				produces cars that are renowned for their exclusivity and 
				performance, it will be able to achieve its objectives in the 
				Omani market with Alfardan Motors, its partner.<br>
				<br>
				Demand for the exclusive character and refined dynamism that is 
				so unique to any Maserati product has risen strongly in the 
				region. As 2011 has proven, the charisma and allure indicative 
				to Maserati cars continues to play an important role for 
				consumers.<br>
				<br>
				Last year alone the company sold 2,715 examples of its flagship 
				coupe, the Maserati GranTurismo. This represented a staggering 
				44 per cent of its overall sales returns for 2011, by far the 
				highest. But interest and demand for the Maserati luxury 
				four-door saloon - the Maserati Quattroporte - is also on the 
				rise. With close to 1,800 units sold, the Quattroporte – a 
				magnificent example of Italian passion and tradition – made up 
				29 per cent of its sales in 2011, with the Quattroporte S 
				leading the way. The versatile and exclusive Maserati 
				GranCabrio, once again proved to be highly desirable across the 
				globe. A total of 1,677 units of the GranCabrio series were sold 
				in 2011. The GranCabrio alone registered a phenomenal 85 per 
				cent of overall sales for the model range.<br>
				<br>
				In total, Maserati, even during a worldwide period of financial 
				instability, generated revenues of 588 million Euros, an 6.4 per 
				cent improvement over 2010. <br>
				<br>
				Speaking on behalf of Maserati, Umberto commented, “We are 
				pleased to have a partner like Alfardan Motors. And we are 
				confident that Alfardan Motors will provide Maserati owners and 
				enthusiasts alike with the true Maserati experience. My 
				confidence is based on the relationship that already exists 
				between Maserati and Alfardan Automotive Group in Qatar, which 
				encompasses more than 15 years of fruitful co-operation and 
				partnership, with a long history of success and achievement. We 
				are certain that this quality will be delivered in Oman and the 
				same success will be demonstrated by Alfardan Motors.”<br>
				<br>
				The exclusive showroom comes to life with a team of trained and 
				dedicated sales consultants, who are pleased to welcome Maserati 
				enthusiasts daily from 8.30 am to 1.30 pm, and again from 4 pm 
				to 8 pm, Saturday through Thursday, while presenting the 
				complete range of Maserati models, including the Quattroporte, 
				the limited edition GranCabrio Fendi, the GranCabrio Sport and 
				the GranTurismo, all of which come with the Maserati Service 
				Package for three years or 50,000 kms, which ensures complete 
				peace of mind and an ownership experience that is second to 
				none. <br>
				<br>
				Meanwhile, the after sales team will be available to provide 
				their technical assistance and support from within a 
				fully-equipped workshop in the proximity of the main showroom, 
				from 8 am to 1.30 pm and 2.30 pm to 5 pm Saturday through 
				Wednesday, and from 8 am to 1 pm on Thursday. The after sales 
				facility is equipped with two working bays and all required 
				parts and accessories for Maserati models within a total 
				servicing solution which can deal with any maintenance and 
				repair requirements.&#8194;</font></td>
