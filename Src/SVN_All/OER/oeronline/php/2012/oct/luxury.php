						<td valign="top">
						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">
						Strengthening its brand presence<br>
						</font><font face="Verdana" size="2">
                		<br>
						Pascal Mouawad, Co-Guardian, Mouawad shares his vision 
						and plans for the company in a candid interview with 
						Mayank Singh<br>
&nbsp;</font></b><p align="center" style="margin: 3px 10px">
				<img border="0" src="images/luxury-oct-12-1.jpg" width="400" height="266"><p align="justify" style="margin: 3px 10px">&nbsp;<p align="justify" style="margin: 3px 10px" dir="ltr">
				<font face="Verdana" size="1">
				<br>
				<b>Mouawad has seen a generational shift recently with the 
				fourth generation taking over the reins of the business 
				recently. What changes can we expect to see in the business as a 
				result of this shift? </b><br>
				Mouawad is a 122-year-old business house which was started way 
				back in 1890 by my great grandfather. He left his native country 
				Lebanon and went to Mexico and the US, where he learnt the 
				craftsmanship of watch making. He returned to Lebanon and his 
				son Fiaz, my grandfather, took over the business. Fiaz decided 
				to expand the business and went to Saudi Arabia, and soon became 
				the royal jeweller for the King and Queen. He also got a Royal 
				Decree to operate a business in Saudi Arabia as a foreigner. My 
				father wanted to be a doctor and went to study medicine in 
				Paris, but at the suggestion of his family he went back to Saudi 
				Arabia to help with the business.<br>
				<br>
				Over 47 years, as the president of Mouawad, he was instrumental 
				in growing the business from a two store operation to a 
				multinational company spanning 12 countries. He took the 
				business to Switzerland, expanded into Asia and at one point had 
				35 stores in Japan. Mouawad also became a big player in the 
				diamond market, acquiring some of the largest diamonds in the 
				world. In 2009 he had a heart surgery and decided to retire by 
				handing over the business to the fourth generation, that is my 
				brother Fred Mouawad and myself. We took over the business in 
				2010 and it is our responsibility to take the business to the 
				next level. We decided to call ourselves co guardians, as we are 
				two of us and our job is to be gate keepers and to strengthen 
				the brand till the next century. <br>
				<br>
				Ten years ago my father had diversified into the real estate 
				business and so to an extent the business was stagnant. We have 
				decided to create a new generation store concept and have 
				redesigned our stores. Our first flagship store opened in Dubai 
				Mall, UAE around two years back. We opened up our regional 
				office in Dubai and are opening a regional factory there too. We 
				have hired a strong management team in Dubai to help grow our 
				business. In the last year and a half we started expanding the 
				number of our stores; we opened our second new generation store 
				in Beirut. We have opened our flagship store in Jeddah, and are 
				opening in Kuwait, Qatar. Our store in Oman is locating inside 
				the Opera Mall. And later this year we will be opening in Abu 
				Dhabi.<br>
				<br>
				As a part of the strategy we decided to move from being a house 
				of brands to a brand house. Historically, we were selling about 
				20 brands in the Mouawad boutiques but we decided to go mono 
				brand with the Mouawad name and we will be selling only our own 
				brand in our stores, with multiple categories like high 
				jewellery which are masterpieces, one of its kind expensive 
				pieces and the collection boutiques which are more affordable. 
				We have always been an aspirational brand, something that was 
				sold to royalty and very wealthy people, so people looked at us 
				and felt that we may be inaccessible. To change this perception 
				we started advertising collections that were more affordable and 
				available at lower price points. For example, the average price 
				point of the boutique collection would be around $5,000 
				(RO2,000) and then there are the masterpieces which have a price 
				tag of $1mn to $10mn. We are also known for our object of arts, 
				which is the third category. We are now focussing on diamond 
				classic, the classics and bridal jewellery.<br>
				<br>
				Finally, the fifth strand of the mono brand strategy is watches. 
				In 1972 my father started a company called Roberje Geneve, and 
				we still carry that brand, but as a part of our new strategy we 
				are going to carry the Mouawad watch brand. We are introducing 
				Little Summer, which are watches under the Mouawad name. To sum 
				up, we are capturing multiple price points across categories, 
				moving into a mono brand strategy and want to have a flagship 
				store in each key city in the region. <br>
				<br>
				How do you plan to convey Mouawad’s new offering and changes to 
				customers in the region? <br>
				We are big believers in marketing and branding, as all customers 
				are looking at buying a dream or brand. We have upped our 
				marketing budget and are very active. Mouawad is amongst the top 
				five advertisers in the region and we advertise in magazines in 
				a big way. Lately we have started advertising on TV. Last year 
				we did a TVC campaign on NBC and we are doing product placements 
				in video clips. We are known as an international brand so we 
				dress certain celebrities in Hollywood, do events and 
				sponsorships. Our advertising budget is in the range of $5mn and 
				it has grown considerably compared to where we were. <br>
				<br>
				<b>Having gone in for a mono brand strategy, are you going to 
				increase your manufacturing capacities commensurately?</b><br>
				We are a fully integrated company with our own office to buy gem 
				stones. Mouawad has done a joint venture with a diamond site 
				holder, so we are becoming partners in a diamond site. We have 
				offices in New York, Antwerp and Bangkok to buy stones; we have 
				our own prototype workshops and a watch factory in Switzerland. 
				Overall, we have a lot of manufacturing capacity and are not 
				concerned about capacity constraints. <br>
				<br>
				<b>Why was the need felt to reinvent the brand? Was there a 
				recognition that somewhere the brand has lost out in the last 
				few years?</b><br>
				The brand is very strong, but it had got a bit stagnant due to a 
				lack of marketing activity, but now we are pulling our weight 
				behind the brand. We are investing behind the brand and we no 
				longer want to put our money behind other brands as we have done 
				in the past. The market was slow in 2009-2010, but things are 
				picking up and we are on a major expansion path, opening five 
				stores in the next six months and there are plans to open 
				additional stores next year. Worldwide we have 14 stores and we 
				are looking at adding five stores every year. <br>
				<br>
				<b>What makes you confident that as a mono brand you can take on 
				stores that are selling some of the best brands in the world?</b><br>
				I am confident that we may perhaps lose some sales in the short 
				term, but we will win in the long term for a number of reasons. 
				First, we do not think that other brands are better than us. We 
				have the know-how and skill sets to make jewellery, design and 
				sell it, so why should we sell other brands? By controlling our 
				distribution we are also controlling our discounts; if you have 
				your own brand you have more margins and this will enable you to 
				advertise more. And by advertising more we will create more 
				demand for our brands. We have had experiences wherein we have 
				built a brand and others have taken it away from us and given it 
				to someone else, so we don’t want to be at someone’s mercy. <br>
				<br>
				<b>It’s been 122 years, but it still remains a family owned 
				business, why have you not professionalised the company? </b>
				<br>
				Ours has always been a family-run business; it used to be a one 
				man business and now it is a two man business. We are definitely 
				thinking of institutionalising the business and possibly making 
				it public in the years to come, but nothing has been decided as 
				of yet, but it is definitely something to think about.&#8194;</font></td>
