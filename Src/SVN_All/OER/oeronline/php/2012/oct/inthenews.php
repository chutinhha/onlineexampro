						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">
						Maintaining steady course in turbulent times</font></b><p align="justify" style="margin: 3px 10px">
						<b>
						<font face="Verdana" size="2">
                		<br>
						World Economic Forum’s Global Competitiveness Index 
						ranks Oman as the 32nd most competitive country in the 
						world, placing it above economic powerhouses such as 
						India, South Africa, Italy and Turkey</font></b><p align="justify" style="margin: 3px 10px">
						&nbsp;<p align="center" style="margin: 3px 10px">
						<img border="0" src="images/inthenews-oct-12-1.jpg"><p align="justify" style="margin: 3px 10px">
				<font face="Verdana" size="1">
				<br>
				With the new centres of economic hope, China, India, Brazil and 
				Russia seeing slow-down, recession in Europe and little growth 
				in the United States, 2012 has proved to be a challenging year 
				for the global economy. However, despite this gloomy backdrop, 
				Oman has kept its position as the 32nd most competitive country 
				in the 2012/13 World Economic Forum’s Global Competitiveness 
				Index, a measure of the economies of 144 countries. This ranking 
				places Oman in the top 25 percentile of the world’s economies 
				and ranks it above economic powerhouses that include India, 
				South Africa, Italy and Turkey.<br>
				<br>
				The Public Authority for Investment, Promotion &amp; Export 
				Development (PAIPED) and the Oman-based think-tank International 
				Research Foundation (IRF), which announced details of the 
				2012/13 World Economic Forum’s Global Competitiveness Index, 
				said this year’s Report is a clear testament to the success of 
				the work being carried out by Omani government institutions in 
				helping raise the Sultanate’s competitiveness and improve the 
				nation’s business environment and above all to the solid 
				economic planning and vision underpinning this.<br>
				<br>
				Conducted by the World Economic Forum in partnership with 
				leading academics and a global network of research institutes, 
				the Index calculates its rankings from publicly available data 
				and more than 15,000 business surveys in the 144 economies.<br>
				<br>
				Senior representatives drawn from across Oman’s private sector 
				gave their input to the Executive Opinion Survey, which provides 
				the basis of the World Economic Forum Report. The survey is 
				carried out by PAIPED and IIR who are part of the World Economic 
				Forum’s network of over 160 Partner Institutes worldwide.<br>
				<br>
				Launching the Report, HE Dr Salem bin Nasser Al Ismaily, 
				chairman, PAIPED emphasised the importance of benchmarking 
				Oman’s competitiveness. “Benchmarking our competitive 
				performance is an important exercise and the 2012/13 World 
				Economic Forum Global Competitiveness Index provides government 
				and the private sector with data that can help formulate policy 
				recommendations that will enable Oman to achieve sustainable and 
				long-lasting competitive gain,” he said.<br>
				<br>
				Noting Oman’s consolidation of its position in the Report, Al 
				Ismaily cautioned against complacency. “Oman maintains its focus 
				on improving policies related to the labour market, education, 
				skills and competition policy,” he said. “At the same time, we 
				must ensure our banking system is providing an adequate supply 
				of credit to support Oman’s thriving enterprise culture.”<br>
				<br>
				<b>Private Sector Perspective<br>
				</b>Speaking on behalf of the private sector, young Omani FMCG 
				business owner and marketing director for Naranjee Hirjee &amp; Co, 
				Nikhil Sampat, said, “Oman’s ranking reaffirms its pro-business 
				stance and openness – both of which play a key role in helping 
				us remain an attractive location for business and trade.”<br>
				<br>
				S Gopalan, CEO, Reem Batteries &amp; Power Appliances Company 
				remarked, “To be successful, we must continue to focus on 
				improving our macroeconomic environment, removing any 
				unnecessary regulatory barriers faced by the private sector. In 
				addition, we have to work hand-in-hand with government to ensure 
				policies and regulations support the ability of domestic 
				companies to be competitive and productive on the international 
				stage. It’s only by building such capacity and institutional 
				policies, will we be able to move to the next stage of improving 
				the Sultanate’s competitiveness and achieve sustained higher 
				levels of prosperity. In this regard, we’ve made significant 
				gains”.<br>
				<br>
				<b>Rankings<br>
				</b>As in previous years, the top 10 remain dominated by a 
				number of European countries: in the overall ranking, 
				Switzerland led the table followed by Singapore, Finland, 
				Sweden, the Netherlands, Germany, the US, the UK, Hong Kong SAR 
				and Japan.<br>
				<br>
				From a regional perspective, several countries from the Arab 
				World, including Oman (32nd); Qatar (11th); Saudi Arabia (18th); 
				United Arab Emirates (24th); Kuwait (37th); Bahrain (35th) and 
				Jordan (64th) occupy the upper half of the rankings.</font></td>
