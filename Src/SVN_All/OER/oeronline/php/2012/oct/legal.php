						<td valign="top">
						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">
						Commercial Agencies Law<br>
						</font><font face="Verdana" size="2">
                		<br>
						This article is the first in a series which gives an 
						insightful analysis of Oman’s legal framework with 
						respect to commercial agencies</font></b><p align="justify" style="margin: 3px 10px">&nbsp;<p align="justify" style="margin: 3px 10px">
						<i><font face="Verdana" size="1">By James Harbridge</font></i><b><font face="Verdana" size="2"><br>
&nbsp;</font></b><p align="justify" style="margin: 3px 10px" dir="ltr">
						<font face="Verdana" size="1"><br>Under Oman’s Law of 
						Commercial Agencies (CAL) – Royal Decree 26/77 (as 
						amended) – a commercial agency is widely defined and 
						includes distributorship and franchise arrangements. 
						Aside from CAL, other pertinent Omani statutes include 
						Ministerial Decision 11/85 (as amended) and also 
						Articles 276-313 inclusive of Chapter 5 of Oman’s 
						Commercial Code (Royal Decree 55/90). For instance, the 
						general statutory limitation period for filing a court 
						case in Oman is ten years from the date when the right 
						of action arose. However, Article 296 of Oman’s 
						Commercial Code states very clearly that: “Actions 
						arising from an agency contract shall not be accepted 
						once three years have lapsed since expiry of the 
						agency.”<br>
						<br>
						<b>Governing law/jurisdiction<br>
						</b>Article 10 of CAL is quite draconian on foreign 
						entities, and reads as follows:<br>
						“The following examples, which are not exhaustive, shall 
						be considered an abusive exercise of rights entailing 
						appropriate compensation:<br>
&nbsp;</font><ol dir="ltr" style="font-size: 8pt; font-family: Verdana" type="A">
							<li dir="ltr">
							<p align="justify" style="margin: 3px 10px" dir="ltr">
							<font face="Verdana" size="1">The termination by one 
							party of an agency contract of unlimited duration in 
							the absence of any breach of duty or any act 
							inimical to the agency relationship being committed 
							by the other party;<br>
&nbsp;</font></li>
							<li dir="ltr">
							<p align="justify" style="margin: 3px 10px" dir="ltr">
							<font face="Verdana" size="1">The principal’s not 
							accepting to renew the term of an agency contract 
							after the end of its original period if the agent 
							can prove that his activity has led to obvious 
							success in the distribution or marketing of the 
							principal’s products and that the non-renewal of he 
							contract is preventing the agent from reaping the 
							benefit expected from his endeavour, unless the 
							principal can prove that the agent has committed a 
							breach of duty justifying the non-renewal;<br>
&nbsp;</font></li>
							<li dir="ltr">
							<p align="justify" style="margin: 3px 10px" dir="ltr">
							<font face="Verdana" size="1">The agent’s 
							relinquishment of the agency at an inappropriate 
							time and without reasonable excuse thus causing 
							damage to the principal.”<br>
							<br>
							In consequence, an often asked question is whether 
							the parties can agree to dis-apply Omani law in 
							their agency contracts covering Oman. The answer is 
							that yes, this is possible. Article 18 of CAL makes 
							it clear that the Oman Courts have jurisdiction in 
							respect of any dispute between principal and agent 
							“unless the two parties have agreed to resort to 
							arbitration”. We see time and time again in Oman 
							that the courts will decline jurisdiction in respect 
							of any contract where the parties have agreed that 
							an arbitral panel will resolve the dispute. The 
							question that then emerged is whether Omani courts 
							would be willing to allow an arbitral panel to hear 
							an agency dispute, where the aggrieved party was an 
							Omani agent seeking remedies under Omani law.<br>
							<br>
							The Omani case law shows that arbitral clauses will 
							be upheld, even when those arbitral clauses allow 
							for arbitration outside Oman and are coupled with a 
							contractual reference to a non-Omani substantive 
							law. In this way, an agency arrangement can cover 
							Oman without it being governed by Oman law.<br>
							<br>
							This is, perhaps, a surprising scenario, given that 
							in the early 1990s an Omani Court judgment 
							determined that the entirety of CAL was an Omani 
							public order law, in the sense that its terms are 
							mandatory. The judgment stated categorically: “…the 
							provisions of the law regulate the relationship 
							between the State as sovereign power and the 
							commercial agencies... which relates to the economic 
							public interest in the state which makes it related 
							to public order…” Furthermore; Royal Decree 29/2002 
							(as amended) – the Omani Court Procedural Law – 
							clearly states at Article 30 that the Omani Courts 
							have jurisdiction to hear claims against non-Omani 
							entities where the contract in question relates to 
							the performance of obligations within Oman.<br>
							<br>
							Nonetheless, the most recent case law shows that the 
							Omani Courts have ruled that aggrieved Omani agents 
							are bound by an arbitral clause and a non-Omani 
							governing law, provided both are explicitly stated 
							within the agency contract in question. The Omani 
							Courts have stated that the parties are free to 
							mutually choose both governing law and jurisdiction 
							as these “relate to the respective private interests 
							of the plaintiff and the defendant under the 
							contract of agency.”<br>
							<br>
							In short, CAL can be prima facie circumvented by a 
							contractual agreement to a non-Omani substantive law 
							and dispute resolution via arbitration. Conversely, 
							if an agency contract referred to, say, “English 
							Courts and English law”, it is probable that the 
							Oman Courts would override such a provision, and 
							themselves take jurisdiction and apply Omani law, 
							including CAL.</font></li>
						</ol>
