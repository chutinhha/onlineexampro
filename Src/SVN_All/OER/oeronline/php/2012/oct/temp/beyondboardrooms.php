						<p align="justify" style="margin: 3px 10px"><b>
						<font face="Verdana" size="4" color="#C00000">
						Giving a cutting edge</font></b><br>
				&nbsp;<p align="justify" class="style13" dir="ltr">
				<b>Alok Ranjan Varma, General Manager of Assarain Concrete 
				Products &amp; Trading, created razor sharp competitive advantages 
				for his company to differentiate itself in a highly crowded 
				market, reports Muhammed Nafie</b><p align="center" class="style13" dir="ltr">
				<br>
				<img border="0" src="images/beyondboardrooms-sep-12-1.jpg" hspace="13" vspace="3" width="400" height="243"><p align="justify" class="style13" dir="ltr">
				<br>
				They say excellence is not a skill but an attitude. It holds 
				true in Alok Varma’s case whose 24-year long career in the 
				construction industry has been studded with relentless pursuit 
				of excellence and unflinching commitment to perfection. Fourteen 
				years with India’s Cement leader Associated Cement Companies 
				(ACC), and 10 years with Assarain Concrete Products &amp; Trading 
				(ACP), one of Oman’s largest concrete manufacturers, helped him 
				get acquainted with a wide range of areas of the industry. It 
				has equipped him with effective management skills and enabled 
				him run businesses in specialised and highly competitive 
				markets. <br>
				<br>
				Wherever he worked, the focus has largely been on business 
				expansion, continuous improvement, aligning people, processes 
				and technology. And driving businesses by creating a higher pull 
				on product movement through brand development, close customer 
				networking and forming partnerships with many key business 
				decision makers.<br>
				<br>
				Ever since he became part of the Assarain family, Varma has 
				concentrated on raising the bar of excellence, increasing brand 
				value and creating a committed team by leading people into 
				participative company building. “I have always tried to create 
				razor sharp competitive advantages for us to differentiate our 
				company in the crowded market,” says Varma. <br>
				<br>
				“Over the last 10 years that I have been heading ACP, we have 
				expanded year-on-year to meet the growing demand of our 
				products. We have created another great, technologically 
				advanced manufacturing hub in Sohar, known as Modern Concrete 
				Products (MCP) to cater for the entire Al Batinah region, 
				Buraimi, Yanqul, Khasab etc. and this facility is fully backed 
				by our competent and progressive professionals with excellent 
				service levels.”<br>
				<br>
				In the recent years, ACP and MCP have made quantum leaps in 
				terms of growth, product range expansion and overall development 
				by providing many other smart constructional products under one 
				roof, and emerging to be the preferred supplier of all industry 
				leaders, government organisations, contracting companies and 
				retail customers. Varma explains, “We are quite buoyant towards 
				future outlook as under the wise guidance and leadership of His 
				Majesty, we see a lot of initiatives taken by the government 
				towards infrastructure development. Hence, ACP and MCP are well 
				poised to become the natural choice of many consultants and 
				contracting companies to contribute towards building these 
				projects with their quality products.”<br>
				<br>
				<b>Leading from front<br>
				</b>The main thrust of Varma’s management principles has been to 
				create productive teams with good inter-departmental weave. “I 
				firmly believe that the paternal management style or 
				‘I-know-more-than-you’ style does not find much ground in 
				today’s world,” he avers. “During my entire career, I have 
				always believed in leading from front and leading by example. 
				People who lead should certainly be knowledgeable, diligent, 
				razor sharp, analytical and yet decisive.” Varma considers 
				himself as a “people’s man” which has always given him an edge 
				to create strong and sustainable partnerships, both outside and 
				within the organisation.<br>
				<br>
				During his leisure time, Varma prefers to relax in a serene 
				place reading books on management or watching intellectual 
				discussions or debates on television. His favourite books 
				include Richard Bach’s Jonathan Livingston Seagull and Jack 
				Welch’s Straight From the Gut. He also likes action movies or 
				thrillers. Safehouse, P2, Wrecked and Iron Lady are some of his 
				favourites, while Turkey, Malaysia, Namibia, New York and London 
				are some of his most enjoyable travel destinations.&#8194;</td>
