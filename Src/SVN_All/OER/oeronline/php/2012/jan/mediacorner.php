				After an exciting battle of grit, nerves and wit, a new champion 
				emerged at the 15th edition of the Times of Oman Open Quiz 
				Contest. Know Brainers (Sujatha Ashok, R S Sumanth, Raji Sumanth), 
				put a stop to the dominance of the defending champions Mostly 
				Clueless to lift the ultimate quizzing trophy in Muscat at the 
				packed-to-the-brim City Amphitheatre recently.<br>
				<br>
				The four-time champions, Mostly Clueless (Dr Anupam Kakaria, 
				Shafeek M K, Shaheen Jamil) and last year’s runners up, 
				Questionable Characters (Rohini Dinesh, Sushmita Gupta, Udayan 
				Gupta) displayed amazing performances to put up a brave fight to 
				finish second and third respectively. <br>
				<br>
				ISG Sreeraag Mohan and team (Sreeraag Mohan T, Darshan Regi 
				Kalathil, R. Rajaganapathy Sankar) were crowned the school quiz 
				champions. ISM Quizzing ISMiites (Rohan Karnawat, Srujan Keskar, 
				Shuvankar Goswami) and ISG Anmol Kakaria and team (Anmol Kakaria, 
				Fahd Rayees, Anirudh Sen) finished first and second runners up 
				respectively. But the true champions of the evening were, of 
				course, the audience, who turned out in impressive numbers 
				despite rain clouds looming large over the skies of Muscat. They 
				didn’t allow the thoughts of inconvenience the rains could have 
				brought and stopped them from attending this Times of Oman event 
				that’s so close to their hearts.<br>
				<br>
				<b>Book on Oman’s greatest brands</b><br>
				<br>
				Oman’s 180 Greatest Brands (2012-13)’, a 400-page book published 
				by Oman Daily Observer recently, pays rich tribute to top-notch 
				brands and brand builders in the Sultanate. Packed with 
				groundbreaking ideas, thought-leading case studies across a 
				range of industry sectors, the book traces the achievements of 
				leading Omani brands and comprehensively investigates their 
				branding strategies to unearth key success factors. Written by 
				Hasan Kamoonpuri, a senior Oman Observer journalist, the book is 
				richly detailed and aspires to be a comprehensive exploration of 
				Omani brands and can very well serve as a barometer for the 
				state of brand management in the country. The book opens with a 
				message from the Minister of Commerce and Industry Shaikh Sa’ad 
				bin Mohammed Al Mardhouf Al Sa’adi. The key part of the book is 
				a comprehensive brand survey across 180 brands in 30 categories. 
				Oman Flour Mills Company has scored a spectacular double in the 
				2011 Observer Brand Awareness Survey, winning the No.1 slot in 
				the wheat flour category as well as in the overall listing of 
				180 greatest brands. So far three brand surveys have been 
				conducted and the key finding common to all is that Omanis are 
				highly brand conscious. Their strong brand habits seem to 
				override the effects of changing prices on their daily purchases<br>
