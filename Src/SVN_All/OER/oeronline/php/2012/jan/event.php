						<span class="style27"><strong>Women of Substance</strong></span><p class="style15">
				<strong><span class="style14"><font size="2">The Al Mar’a 
				Excellence Awards recognised and honoured exemplary women 
				achievers</font><br>
&nbsp;</span></strong><p align="center" class="style13">
				<img border="0" src="images/event-jan12-1.jpg" width="495" height="229" style="border: 1px solid #C0C0C0"><p align="justify" class="style13">
						<br>The Al Mar’a Excellence Awards, the 
						first-of-its-kind in the Sultanate honoured women 
						achievers across different fields at a glittering event 
						at The Amphitheatre, Shangri-La’s Barr Al Jissah Resort 
						&amp; Spa on December 5.<br>
						<br>
						The event was held under the auspices of Her Highness 
						Sayyida Dr. Muna bint Fahd Al Said, Assistant 
						Vice-Chancellor for External Co-Operation, Sultan Qaboos 
						University. A number of other dignitaries and the who’s 
						who of Muscat were also present at the event. <br>
						<br>
						The Al Mar’a Excellence Awards were presented across 12 
						categories; namely: Corporate Leadership, Fashion 
						Design, Performing Arts, Fine Arts, Educational 
						Services, Entrepreneurship and Innovation, Health 
						Services, Science and Industry, Technology, Sports, 
						Petroleum Services and Social Responsibility. Apart from 
						these, two women were the recipients of the prestigious 
						Woman of the Year and Most Promising Woman of the Year 
						Awards. Special Awards were given to four women who have 
						made significant contributions in the fields they have 
						chosen to be in.<br>
						<br>
						The Awards were judged on the basis of nominations 
						received which were then reviewed by a panel of esteemed 
						judges and the final ranking process was appraised by 
						Ernst &amp; Young. The Awards judged women on the basis of 
						criteria like innovation and creativity, the impact 
						their contributions have had in society, leadership 
						abilities and the goals achieved.<br>
						<br>
						The red carpet event also featured some dazzling 
						entertainment choreographed by Emmanuel Castis, a 
						talented international vocalist with many music awards 
						to his credit. <br>
						<br>
						The Al Mar’a Excellence Awards was a wonderful platform 
						that not only felicitated woman achievers; but also 
						revealed the passion and inspiration of women who have 
						aspired to attain great accomplishments in their chosen 
						fields and have gone that ‘extra mile’ to turn their 
						dreams into reality.<br>
