                  <td width="100%"><font face="Verdana" size="4" color="#FF0000">6-appeal</font><p><font face="Verdana" size="1"><b>The new generation Mazda6 is now on your television screens and in Oman's showrooms. Nayl D'Souza test-drives the  car and looks at Mazda's new brand identity</b></font></p>
                    <p><font face="Verdana" size="2"><img border="0" src="../../images/2002_july/mazda-1.jpg" align="left" hspace="5" vspace="5" width="225" height="126"></font><font face="Verdana" size="2">It seems like only yesterday we were told that we would be amazed with a Mazda. To be honest, more often than not, I was. Today, Mazda Motor Corporation (MMC) has impressed me once again. ‘Zoom-zoom’ is the latest, highly entertaining and memorable slogan accompanying the introduction of the Mazda6, a new midsize family sedan and hatchback. The car’s worldwide launch marks the beginning of a new broad brand strategy from Mazda. The Japanese car giant aims to steer its overall auto brand into a new direction that helps customers perceive Mazda as clearly distinct, more sophisticated and relevant to their expectations.&nbsp;<br>
                    Zoom-zoom is intended to signify the fast movement and joyful abandonment kids simulate at playtime with toy cars. If successful, this attempt to rekindle the childhood notion of ‘emotion in motion’ in adults could attract 200,000 global sales of the new series alone. This innovative mantra from Mazda is also driven by a new brand promise of delivering stylish, insightful and spirited dynamic performance vehicles. “The design of Mazda6 establishes clear direction for the new Mazda brand,” says Iwao Koizumi, Chief Designer for Mazda6. “With Mazda6 - and many new vehicles to follow - we are returning to our roots as a builder of fun-to-drive vehicles. Cars like the legendary Cosmos Sport, RX-7, and MX-5 Miata roadster,” adds MMC President Mark Fields.&nbsp;<br>
                    So will the Mazda6 succeed in winning the hearts and minds of people who love cars and want a vehicle with bags of personality? To find out, OER visited Towell Auto Centre, the authorised distributor of Mazda vehicles in the Sultanate, to give the new Mazda6 (saloon base model) a spin around Muscat.&nbsp;<br>
                    <br>
                    <b>The looks</b><br>
                    The Mazda6 gets eight out of ten for good looks. It has got an exceptionally sharp, sleek and athletic appearance. Despite having no real corners on the vehicle, the 6 still maintains a sense of boxy packaging. Moreover, the headlamps set into elegantly fluted chrome lamp housing are very dapper while the front grill resembles a menacing smile (as sinister yet affectionate as that of Darth Vader). The car’s animated appearance is a worry to manufacturers of competing vehicles in the same segment such as the Volkswagen Passat, Honda Accord and the Toyota Camry. The bold well-crafted rear and bulging sides suggests that Mazda’s benchmarking of the BMW 3-series has also successfully paid off. Meanwhile, Mazda’s signature badge near the trunk is slightly bigger than usual. The number ‘6’ in italic on the car’s badge denoting speed and flair is another nice touch.<br>
                    <br>
                    <b>The feel</b><br>
                    The interior of the Mazda6 is also a joy to behold insofar as it is as unique as the car’s outer dynamism. The airy interior provides a great deal of comfort for driver and passengers. The vehicle is 50mm wider than other cars in its segment. Mazda put this extra width to good use with generous roominess for all occupants of the vehicle. The legroom for backseat passengers is vast while the front shoulder room is also impressive.<br>
                    The Mazda6 offers an equally spacious trunk. Its 501-litre volume of usable space exceeds many brands in its class by over two full-to-bursting-point carrier bags. The load floor is very flexible in dealing with wide and long loads such as flat-packed furniture items. Best of all, a unique design feature - inspired by a Japanese mechanical doll called a karakuri - makes it possible to fold the backseats down in just one touch making it quick, easy and convenient to open up all of the car’s carrying capacity.&nbsp;<br>
                    The car also gifts passengers with a sporty T-shaped instrument panel with titanium-like finish. The panel’s centre stack houses a simple yet lovely designed music system. The stack’s LCD with its ‘hello’ greeting after engine ignition is slightly tacky and the circular flaps on the AC vents are just as naff but these glitches are forgivable when discovering the large number of thoughtfully-placed stowage places for cups, bottles and papers. Comfortable seats, individual courtesy lights in the front and rear, high-mount brake lamp, and one-touch power windows on the driver’s side add extra convenience.</font></p>
                    <p><font face="Verdana" size="2"><img border="0" src="../../images/2002_july/interior.jpg" align="right" hspace="5" vspace="5" width="250" height="151"><b>Safety and security</b><br>
                    The Mazda6 employs new safety technology that uses an advanced Crash Zone Sensor (CZS) at the front of the vehicle to determine the severity of any frontal impact. It then signals a sensor control module unit inside the vehicle to deploy the dual-stage inflation front airbags as necessary for optimum protection. The body structure has built-in side impact protection bars and the front seatbelts feature smart pretensioners and load-limiters. Meanwhile, an immobiliser fitted as standard adds a sense of security.</font></p>
                    <p><font face="Verdana" size="2"><b>Performance</b><br>
                    <img border="0" src="../../images/2002_july/style-excite.jpg" align="right" hspace="5" vspace="5" width="200" height="146"> Overall, the performance of the Mazda6 does not disappoint. A 2-litre all-aluminum 4-cylinder engine powers the car. The acceleration is vivid as promised and the roadster’s torque is maintained at high speeds. Although there have been reports that the Mazda6 has an upsetting degree of road noise at motorway speeds, I couldn’t hear any unruly engine sound intruding into the cabin at 120km/h. The silence was complimented by an interior totally free of annoying squeaks and rattles from road vibrations.&nbsp;<br>
                    The Mazda6 has alert road handling. The car practically hugs the blacktop. There’s responsive braking too where the braking capability has been measured at a short 37-metre braking distance from 100km/h to zero. The all-new platform of Mazda6 is available in automatic and manual transmission.&nbsp;<br>
                    <br>
                    <b>The verdict</b><br>
                    Whether or not the Mazda6 can beat the superb quality and reliability of the Honda Accord with its excellent ride and handling is debatable but it certainly has more boot space for luggage and groceries. The 6 will also find it difficult to dent the image, style and excellent chassis of the BMW 3-series although at least there is much less of a squeeze for two large/tall people in the cabin’s rear.&nbsp;<br>
                    When competing against the Volkswagen Passat, many may find the Mazda struggle to offer as much family comfort and efficiency despite being economical, offering a sportier feel and an engine performance to match.&nbsp;<br>
                    Starting at around RO5,000, the Mazda6 is very reasonably priced and warranty is extensive. Versions above the base model also pack bonuses such as anti-lock braking system, brake assist, more refined upholstery, automatic rear mirror, and even more interior storage compartments.<br>
                    The Mazda6 is a very attractive package in a highly competitive sector. Only time will tell if people decide to buy a car with plenty of zoom-zoom. My guess is that the Mazda will convince many a family man that they can afford to drive their lovely wife in a sporty-looking sedan that also has more-than-adequate space for 2.4 kids and the mother-in-law to boot.&nbsp;</font></p>
