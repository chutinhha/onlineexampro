            <p align="justify"><font face="Times New Roman" size="4">T</font><font face="Verdana" size="1">he 
            share price growth of the top five companies exceeded the average 
            growth of the MSM index of 42 per cent. Dhofar Insurance is on top 
            of the list of companies registering the best share price growth in 
            the year 2003. The chairman of Ominvest, Taya Bin Jandal Bin Ali, 
            has stated in his report to the shareholders that during 2003, 
            accommodative monetary policies were adopted worldwide to ward off 
            deflation and to boost growth. These measures enabled the US and 
            other global markets to stabilise and generate favourable 
            environment for investments. <br>
            <br>
            In Oman, the growth in the economy outpaced budgeted projections and 
            this was reflected by a vibrant stock market. Jandal adds that in 
            this positive environment, the group's investments performed 
            creditably and the profit for 2003 recorded a 54.38 per cent 
            increase over 2002. Jandal states that their major subsidiary, Oman 
            Arab Bank, reported improved results, which were attributable to 
            substantial increase in interest margins, and gains from MSM-related 
            investments. The board has recommended a 30 per cent cash dividend 
            for the year. Jandal is hopeful that relatively stable oil prices 
            and low interest rates should again generate favourable business 
            conditions in Oman and elsewhere. The group should, therefore, 
            perform satisfactorily and would report a profitable year in 2004.
            </font></p>
