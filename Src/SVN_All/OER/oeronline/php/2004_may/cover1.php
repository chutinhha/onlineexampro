            <p align="justify"><font face="Times New Roman" size="4">T</font><font face="Verdana" size="1">he 
            top five earnings per share growth companies are identical to the 
            top five by growth of profit. The chairman of ONIC Holding, Salim 
            bin Hassan Macki, has stated in his report to the shareholders that 
            during the year-ended March 2003, the Steering Committee continued 
            its mission to restructure the company and bring it on a stable 
            footing for future years. Macki stated that the company restructured 
            its capital and negotiated a settlement agreement with Al Yousef. 
            Macki adds that the initiatives have enabled the company to achieve 
            satisfactory results, which were largely due to profits from 
            subsidiary companies, write-back of provisions and revaluation of 
            investments. The Committee recommended a 9 per cent cash dividend 
            for the year. Macki adds that the company is poised for further 
            growth and profitability in the years ahead. </font></p>
