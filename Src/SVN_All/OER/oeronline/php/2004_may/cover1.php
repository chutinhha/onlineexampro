            <p align="justify"><font face="Times New Roman" size="4">O</font><font face="Verdana" size="1">nce 
            again, four of the top five companies in Oman having the largest 
            market capitalisation are banks. The chairman of NBO, Khalfan Bin 
            Nasser Al Wohaibi, in his report to the shareholders has stated that 
            it is a source of ongoing regret that throughout 2003, the magnitude 
            of the problems of the years 2001 and 2002 continued to predominate 
            over the operating profitability of the bank. The overall outcome of 
            the bank's operations for 2003 was a net loss of RO51.776 million 
            compared to a net loss of RO0.262 million reported in 2002. Al 
            Wohaibi explains that these results were primarily influenced, for 
            the third consecutive year, by a heavy level of provisioning for 
            impaired indebtedness aggregating to RO70.7 million. <br>
            <br>
            Al Wohaibi states that in terms of balance-sheet evolution, deposits 
            grew by 4.9 per cent while the loan portfolio was reduced by RO81.7 
            million as part of the qualitative improvement exercise to forestall 
            the likelihood of further impaired indebtedness arising in the 
            future. Al Wohaibi adds that notwithstanding these very 
            disappointing results, the bank's capital strength was enhanced 
            during the year by the subscription of 24,195,406 additional shares 
            with a nominal value of RO1 at a premium of 120 per cent by Suhail 
            Bahwan Group (Holding) LLC. Al Wohaibi confirms that throughout 
            2004, the bank will focus on the twin strategies of prudent 
            profitable business development and dynamic remedial management so 
            as to enhance value for their shareholders and all the other 
            stakeholders. </font></p>
            <p align="center">
            <img border="0" src="im20.jpg" width="298" height="135"></p>
            <p align="justify"><font face="Times New Roman" size="4">I</font><font face="Verdana" size="1">nterestingly, 
            none of the top five companies showing the best return on equity 
            employed come from the banking sector. Salim Mubarak Al Shanfari, 
            the chairman of Dhofar Insurance Co., states that the expectations 
            as well as the exceptional results attained by the company were 
            outstanding by all standards. Total premium income for 2003 amounted 
            to RO35.87 million compared to RO37.29 million for 2002. <br>
            <br>
            Shanfari goes on to say that the imposition of strict terms by the 
            re-insurers in the aftermath of September 11, 2001, has led the 
            company to adopt a new strategy to increase its retention capacity. 
            To enable this strategy to work, the company adopted an increase in 
            the share capital by 25 per cent through an issue of bonus shares, 
            thus increasing the paid-up capital to RO9.375 million. The 
            company's board has recommended a 25 per cent cash dividend for the 
            year. Shanfari states that the increase in the share capital will 
            provide the company with a solid financial foundation aiming to 
            provide better underwriting facilities to retain higher portion of 
            the risks in accordance with the company's policy to reduce 
            dependency on the international markets.</font></p>
