                    </font><font size="4" color="#FF0000" face="Verdana">The OER 
            20 - The Roll of Honor</font><p align="justify"><b><font face="Verdana" size="1">
            A 14 
            per cent increase in the average oil prices and a 6 per cent 
            increase in income from LNG saw Oman's economy end the year 2003 on 
            a positive note with the local stock market displaying bullish 
            investor sentiments and recording an increase of 42 per cent 
            year-on-year. <br>
            <br>
            The OER 20 analysis and ranking by Mukhtar Hasan, based on last 
            year's financial results of Oman's largest listed companies, 
            provides a 20/20 view of the progress made by the country's top 
            blue-chips. Like last year, The OER 20 has been verified by KPMG 
            this year as well.</font></b></p>
            <p align="justify"><b><font face="Verdana" size="1">
            <img border="0" src="im14.jpg" align="left" hspace="5" width="250" height="220"></font></b><font face="Verdana" size="1">The year 2003 has 
            been positive for O</font><font face="Verdana" size="1">man's economy in general and significantly 
            bullish for the Muscat Securities Market (MSM) in particular. We saw 
            an increase in the price of oil during the year 2003 with average 
            price increasing by about 14 per cent to $27.63 (RO10.63) per 
            barrel. This price increase more than compensated the fall in oil 
            production from 897,000 barrels per day (bpd) to 824,000 bpd. 
            Moreover, 2003 also saw a 6 per cent increase in income from LNG. 
            This resulted in an improvement in the planned budgeted deficit. 
            Cash liquidity during the year eased and liquidity of stocks 
            improved substantially. Trading volumes amounted to RO593 million in 
            2003 compared to RO231 million in 2002, an increase of 157 per cent. 
            The MSM index, which started the year at 191.86 and closed it at 
            272.67 - an increase of 42 per cent - was the sixth best performing 
            Arab stock market in 2003 after the Kuwait, Saudi, Doha, Amman and 
            Dubai stock exchanges. <br>
            <br>
            During the course of 2003, all the listed companies adopted the 
            corporate governance regulations introduced by the Capital Market 
            Authority in 2002. This, together with excess liquidity and soft 
            interest rates, has no doubt played a part in the bullish 
            performance of the stock market during 2003 for maximising 
            shareholder value. The government continued its emphasis of 
            diversifying the economy away from dependence on oil. Several mega 
            projects were launched including the Sohar Port, Sohar Refinery, 
            Chemical Fertiliser plant, Batinah Power Plant etc., some of which 
            have already gone into the construction phase. <br>
            <br>
            The government continued to pursue its privatisation policy. 
            Important developments in privatisation initiatives include power, 
            telecommunications, water, airports, ports, wastewater, postal and 
            surface transport sectors. Substantial progress has been made during 
            2003 with regard to tourism. The easing of visas should have a major 
            impact on this industry in the future. <br>
            <br>
            On the international scene, after the traumatic losses of the last 
            three years, the decisive rebound in 2003 helped make up for some of 
            the losses investors have suffered from 2000 through 2002. All the 
            main indices showed handsome gains of between 25 and 50 per cent. 
            The year started on a cautious note but the mood changed quickly 
            after the end of the Iraq war in March. A significant turnaround in 
            the US economy was achieved with the assistance of a 45-year low in 
            interest rates, the new tax-stimulus package and the weak US dollar. 
            During 2003, the revenues of Oman's 20 largest companies showed a 
            modest growth. Total revenues for The OER 20 went up by 1.5 per cent 
            to RO887 million. After a good corporate performance of 2002, 
            overall, the profits for 2003 decreased dramatically by 41 per cent 
            to RO53 million from the figure of RO90 million last year. The main 
            reason for this dismal performance was the substantial loss of RO52 
            million posted by National Bank of Oman, which completely distorts 
            the overall figure. If this loss is excluded from the total profit 
            numbers, the performance of the other companies would show a growth 
            of 17 per cent to RO105 million. <br>
            <br>
            The average P/E ratio of The OER 20 based on the profits of 2003 and 
            the share price on April 15, 2004, is 25.9 times earnings. Once 
            again, this number is greatly distorted due to the substantial loss 
            of NBO. After removing the NBO loss, the average PE works out to 
            13.1 times</font></p>
            <p align="left"><b><font face="Verdana" size="1">Who's in, who's 
            out?<br>
            <br>
            </font></b><font face="Verdana" size="1">Only one company that was 
            there last year did not make The OER 20 this year. Oman National 
            Electric Co. (ONEC) dropped out because a newcomer - Oman National 
            Investment Holding Co. - had a higher turnover. Due to the 
            non-availability of the financials of ONEC, their turnover had to be 
            estimated based on their last submitted financials up to September 
            2003. <br>
            <br>
            The ranking of Oman's 20 largest companies in order of revenue 
            produces a list which includes five major banking institutions, two 
            insurance companies, five companies from the industrial sector and 
            seven companies from the services sector.
            </font></p>
            <p align="center"><font face="Times New Roman" size="4">
            <img border="0" src="im15.jpg" width="295" height="138"></font></p>
            <p align="justify"><font face="Times New Roman" size="4">T</font><font face="Verdana" size="1">he 
            top five companies in Oman remain unchanged from last year, except 
            for OAS going up one place to fourth position and Oman Oil (formerly 
            BP) going down to fifth place. It is concerning to note that four of 
            the top five companies have shown a decline in revenue for 2003. 
            Shell Oman Marketing Company continues to remain by far the largest 
            public company in Oman. The company showed a negative growth of 4.83 
            per cent during the year 2003. The chairman of the company, Dr 
            Andrew Wood, in his report to the shareholders explains that 2003 
            has been a challenging year. The security situation in the region 
            combined with the SARS outbreak in the Far East influenced a number 
            of key factors. <br>
            <br>
            Dr Wood states that the completion of the gas pipeline project that 
            brought gas to the south of the country, while a significant 
            national achievement, led to a sharp decline of liquid fuels demand 
            in the commercial fuels market. Meanwhile, high crude oil prices 
            worldwide caused a steep rise in the base oil import prices and had 
            a significant effect on the overall results. Dr Wood stated that 
            despite the downward pressure, the company delivered encouraging 
            results close to last year's performance. <br>
            <br>
            Shell has declared a dividend of 600 baiza per share, the same as 
            the last two years - a dividend yield of 8.63 per cent. Dr Wood 
            cautioned the shareholders that the adverse trends in market fuels 
            volumes would leave a gap in the projected performance for 2004. 
            Although the company will continue its growth mode, the profit for 
            2004 is expected to decline. Achieving profitability similar to that 
            of 2003 will realistically only be possible over the next 2-4 years, 
            driven through growth in the retail business.</font></p>
            <p align="center">
            <img border="0" src="im16.jpg" width="298" height="139"></p>
            <p align="justify"><font face="Times New Roman" size="4">F</font><font face="Verdana" size="1">our 
            of the top five most profitable companies in Oman are banking 
            institutions. Other than OIB, which shows a negative growth in 
            profits, the other four companies show substantial growth rates for 
            the year. BankMuscat was once again the most profitable company in 
            Oman as well as the second largest company in the country based on 
            turnover for 2003. The bank has maintained this position by 
            attaining a growth of 18 per cent in profit for the year. <br>
            <br>
            Its chairman, Abdul Malik bin Abdullah Al Khalili, states in his 
            report to the shareholders that during the year, the bank 
            successfully completed a strategic investment in Centurion Bank in 
            India, and acquired the loan portfolio of Citibank in Muscat. The 
            bank was yet again declared 'Best Bank in Oman' by three 
            international financial publications. Khalili goes on to say that 
            BankMuscat commands a market share of over 34 per cent in terms of 
            total assets, 37.7 per cent of total credit and over 34 per cent of 
            total customer deposits. The bank's board has proposed a cash 
            dividend of 25 per cent and a stock dividend of 5 per cent for 2003. 
            Khalili confirmed that BankMuscat remained committed to become one 
            of the foremost banks in the region by the turn of the decade.</font></p>
            <p align="center">
            <img border="0" src="im17.jpg" width="298" height="137"></p>
            <p align="justify"><font face="Times New Roman" size="4">R</font><font face="Verdana" size="1">enaissance 
            Services has shown the highest growth in profits by a staggering 
            267.21 per cent. Samir Fancy, the chairman, in his report to the 
            shareholders states that it has been a remarkable year for the 
            company. The company encountered major challenges and significant 
            opportunities, and responded positively to deliver good results, 
            improved shareholder value and healthy cash flows. Fancy states that 
            investors also recognise the inherent value in the company's four 
            core businesses even though there have been varying contributions in 
            profit from operations. <br>
            <br>
            Fancy adds that the Contract Services Group produced the outstanding 
            result of the year. The company's investments in building, owning 
            and operating facilities for oilfied contractors have proven their 
            worth. The business has delivered growth again this year and 
            provided the foundation for the company to pre-qualify and compete 
            on the international stage; including winning short-term build, own, 
            operate (BOO) projects in Iraq. The company's board has proposed a 
            cash dividend of 25 per cent and a stock dividend of 5 per cent for 
            2003. Fancy confirms that going forward, the future prospects of the 
            company remain extremely positive. </font></p>
            <p align="center">
            <img border="0" src="im18.jpg" width="297" height="137"></p>
            <p align="justify"><font face="Times New Roman" size="4">F</font><font face="Verdana" size="1">our 
            of the top five companies that have the highest amount of equity 
            employed are banks. The only change in this list from last year is 
            that BankDhofar has replaced United Power Company in the fifth 
            position. <br>
            <br>
            Oman International Bank (OIB) is the second highest capitalised 
            company in Oman. Reem Omar Zawawi, the chairperson, states in her 
            report to the shareholders that the bank completed its 20th 
            anniversary during 2003. Zawawi states that the bank's performance 
            for the year was encouraging. The operating profit for 2003 was 
            RO15.46 million compared to RO16.86 million in 2002. This decrease 
            was anticipated in view of the bank's decision to implement 
            stringent credit risk management principles to ensure a significant 
            improvement is asset quality. <br>
            <br>
            The bank's success in recovering several major non-performing loans 
            totalling RO13.5 million resulted in a bottomline impact of RO5.7 
            million, a significant contribution. Zawawi goes on to say that the 
            strategy of focusing on improved credit quality led the bank to exit 
            a number of relationships that did not meet their credit standards. 
            Accordingly, the Loans and Advances stood at RO388.02 million as at 
            December 31, 2003, compared to RO403.96 million as at December 31, 
            2002. Total Deposits stood at RO472.73 million as at December 31, 
            2003, compared to RO458.90 million as at December 31, 2002. <br>
            <br>
            The bank's board proposed a cash dividend of 21 per cent for 2003. 
            Looking forward, Zawawi is confident that the prudent macro economic 
            policies of the government of the Sultanate will provide ample 
            opportunities for banks to meet the needs of their customers. <br>
            <br>
            Zawawi adds that this, together with the solid business franchise 
            that the bank enjoys in Oman, leads the board to believe that OIB 
            will be successful in expanding and developing its business, and 
            thereby continuing to contribute to the progress of the nation.
            </font></p>
            <p align="center">
