            BANKDHOFAR</font></b><font size="4" color="#FF0000" face="Verdana"><br>
            </font><font face="Verdana" size="4">GET SET… GROW</font></p>
            <p align="left"><b><font face="Verdana" size="1">It may have dropped 
            a notch in the OER 20 rankings – from No. 9 last year to No. 10 this 
            year – but BankDhofar has shown one of the best improvements in 
            terms of the vital financial indicators for a bank: loans and 
            deposits</font></b></p>
            <p align="justify" dir="ltr"><font face="Verdana" size="1">
            BankDhofar SAOG (formerly Bank Dhofar Al- Omani Al-Fransi SAOG) had 
            an eventful<img border="0" src="ahm.jpg" align="right" hspace="5" vspace="2" width="100" height="136"> 
            2003, with the implications of the merger with Majan International 
            Bank a year ago being reflected on the balance-sheet this year. 
            Evidently, the bank’s equity rose from RO47.408 million in 2002 to 
            RO63.127 million in 2003, a jump of 33 per cent, the most among the 
            five local banks in the country. Also, during the second quarter of 
            2003, an international consultancy firm concluded its review and 
            study of the various organisational and managerial aspects of the 
            bank after the merger and its recommendations were discussed and 
            approved by the board and have been implemented. Besides this, the 
            bank’s board retained a renowned consultancy firm to coordinate and 
            supervise its five- ear strategic plan, which was presented, 
            discussed and approved by the board in the second half of 2003. <br>
            <br>
            “Last year was an excellent year for the bank. We
            <img border="0" src="infor10.jpg" align="left" hspace="5" vspace="2" width="200" height="317">met 
            all our targets. The bank has been showing consistent growth&nbsp; 
            every year since 1995,” claims Ahmed Ali Al Shanfari, the chief 
            executive of BankDhofar. “Since last year, the new (postmerger) 
            structure of the bank is also in place, including all the 
            committees. The only&nbsp; thing that is left now is to capitalise 
            on it and make sure that we continue to achieve the growth we desire 
            and we continue to mitigate the risk,” he adds. <br>
            <br>
            <b>The numbers are growing </b><br>
            The bank comes tops among all the five local banks in the Sultanate 
            in terms of the crucial parameters of Return on Average Equity 
            (18.38%); Return on Average Assets (2.48%); Operating Expense to Net 
            Operating Income (41.66%); and Net PAT per Employee (RO18,070). If 
            you need further proof of this young bank’s coming of age, here’s 
            it:<br>
            • Total assets of BankDhofar grew by 37.8% and reached RO474.1 
            million at the end of 2003<br>
            • The net profit of the bank, after making the required provisions 
            for bad and doubtful debts, grew in 2003 by 22.4% and reached 
            RO10.16 million compared with RO8.30 million in 2002<br>
            • Earnings per share improved from RO0.235 in 2002 to RO0.252 in 
            2003<br>
            • Its net customer loans portfolio increased from RO266 million in 
            2002 to RO367.2 million, a growth of 38.1%<br>
            • Customer deposits of BankDhofar recorded a growth of 29.3% and 
            increased from RO277.2 million at the end of 2002 to RO358.4 million 
            at the end of 2003<br>
            • Share capital increased from RO35.28 million at the end of 2002 to 
            RO41.96 million at the end of the current year due to the issuance 
            of 6.68 million shares with par value of RO 1 each to the former 
            shareholders of erstwhile Majan International Bank as part of the 
            merger consideration.<br>
            • As a result of the increase in the shareholders’ equity, the net 
            assets per share increased from RO1.344 at the end of 2002 to 
            RO1.504 at the end of 2003.<br>
            <br>
            Moreover, most profitability indicators too recorded a significant 
            growth, as net interest income grew by 17.6% from RO18.2 million for 
            the year 2002 to RO21.4 million for 2003. Other income increased by 
            23.8% and reached RO5.04 million in 2003 compared with RO4.07 
            million in the previous year. The bank’s board has recommended a 
            RO6.29 million cash dividend that represents 15% of the share 
            capital of the bank.<br>
            <br>
            <b>Growth by design</b><br>
            When the bank underwent a corporate identity change earlier this 
            year, Shanfari had said: “Our new identity – from logo to typography 
            to core message – reflects the values and belief that have laid the 
            foundations for our current success, while noticeably stating that 
            we have big plans for the future.” It’s a young bank, no doubt, but 
            things are sure getting bigger all the time.<br>
            <br>
            Shanfari’s confidence in the future is, indeed, not a pie-inthe- 
            sky, stemming from the fact that it has a “very committed board and 
            a dedicated and loyal staff that are constantly working hard to 
            improve the quality of the bank’s services and products.” Little 
            wonder, then, that the bank’s new marketing slogan says ‘Moving 
            forward…with you’.<br>
            <br>
            “We focus on our strengths and capitalise on them; identify the 
            weaknesses and try to rectify them wherever possible; making sure 
            that the quality of the bank’s products and services is always 
            enhanced,” claims Shanfari. “Our performance speaks very clearly as 
            to what we are about as a financial institution. We are financially 
            strong – we have achieved consistent growth, and have had a steady 
            and increasing share price,” he adds.<br>
            <br>
            Traditionally, the bank’s growth strategy has been one by 
            acquisition. It was the Muscat branch of Banque Paribas in 1990, the 
            bank of Credit &amp; Commerce International in 1992 and, more recently, 
            the Majan International Bank in 2002. However, as Shanfari puts it, 
            “We did follow acquisition as a growth strategy traditionally, but 
            there is organic growth as well. Acquiring other banks has 
            strengthened the customer base, the branch network and the ATMs, but 
            we have grown naturally as well. For instance, after the latest 
            acquisition of&nbsp; Majan International Bank, we retained only two 
            of its branches while the rest of the 43 operational branches are 
            our own. Similarly for ATMs.”<br>
            <br>
            True, but is healthy organic growth really possible in a market like 
            Oman with more than a handful of players fighting for a relatively 
            smaller cake? “The market is small,” admits Shanfari, “but we are 
            hoping that the economy as a whole will grow to allow us to grow 
            further. We hope that the oil price stays between the $22-$25 band, 
            and that the revenues from gas and those from privatisation of 
            government-owned companies will help, and will show the foreign and 
            the local investor that Oman is the place to invest in… We talk 
            about indirect investment, but we want to see some direct investment 
            as well. We&nbsp; hope that the economy will not stay small for too 
            long.”&nbsp; <br>
            <br>
            Isn’t that unrealistic? Counters Shanfari: “Most of the 
            infrastructure of the country has either been built or is in the 
            process of being built. The amount of students graduating puts a lot 
            of pressure on not only the government but also the private sector 
            to open up. This opening up should include relaxation of a lot of 
            procedures in Oman – be it in tourism or on the industrial side. I 
            think, so far, we have lost a lot of time. We cannot afford to lose 
            any more time.”<br>
            <br>
            “We are lucky to have the oil price at the level it is today. In 
            fact, taking into account the inflation, the value of the dollar is 
            also low. Adjusting for inflation, the value of today’s $30 is 
            equivalent to only $6-7 of what it was in the 1970s. So we have to 
            ensure growth of the non-oil sectors of the country,” he says. Is 
            Bank- Dhofar looking at markets outside Oman as well? “We are 
            looking everywhere. We may not have a precise strategy to go outside 
            Oman, but the whole GCC is our customer,” he answers.<br>
            <br>
            <b>Growth areas</b><br>
            “We basically have two segments – corporate and retail loans,” 
            reveals Shanfari. “Of course, the retail books are profitable, but 
            they are also associated with the highest risk. The corporate books 
            are profitable as well, but the risk associated with them is lower. 
            We also have a host of other retail products as well, like Visa and 
            the Al Afdal, Al Menar and Al Heson accounts, all of which have 
            contributed to our bottomline,” he adds.<br>
            <br>
            Talking about the growth strategy of the bank, Shanfari explains 
            that “apart from adding new products, the bank is now working 
            towards automating existing products and making them available 24 
            hours a day, 365 days a year.” The bank has made significant 
            investments in state-of-the-art banking technology, which promise to 
            give its customers convenient banking access, better service and 
            greater value for money. “We understand the way world banking is 
            shaping up, the new identity reflects where we come from, where we 
            stand today, where we are headed and what you mean to us” he added.<br>
            <br>
            Shanfari acknowledges that the bank’s success is a result of keeping 
            its customers happy. A priority feature in its future plans is 
            constantly keeping abreast with the latest changes in world banking 
            technology, thus never losing sight of its allimportant ethic - 
            making banking easier for its customers.</font></p>
