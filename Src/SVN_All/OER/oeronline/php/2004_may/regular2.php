<p><font face="Verdana" size="4" color="#FF0000">Car 
            Review<br>
<br>
</font><b><font face="Verdana" size="2" color="#FF0000">It takes 3 to tango</font></b></p>

<p align="justify"><font face="Verdana" size="1"><b>The all-new 2005 Mazda3 gets 
an AAA+: It's active, adaptable and affordable. The plus? Well, it's a Mazda! 
Vicky Kapur takes the compact car out for a spin around town</b></font></p>

<p align="justify"><font face="Verdana" size="1">
<img border="0" src="im6.jpg" align="left" width="300" height="187">The all-new 
2005 Mazda3 line of compacts is a driver's delight. The Mazda3, available as a 
four-door sedan in Oman, is bigger, more powerful, more fuel efficient, safer 
and better looking than its predecessor. What else could you ask for in a 
compact car beginning a new generation? Of course, it's cheaper too, when you 
compare the value you derive out of your buck.<br>
<br>
Beginning at RO5,000 for the base 1.6S model and going up to RO7,000 for the 
top-of-the-line 1.6V Sport variant, the Mazda3, with a 1.6-litre standard engine 
for all its four variants, is the latest in a model revamp that so far has 
brought the Mazda6 to the Sultanate. These are cars with not only more fire in 
terms of performance, handling and style, but are also affordable, and have 
helped the company achieve critical product acclaim from customers and media, 
including more than 50 international awards for the Mazda6.<br>
<br>
The newest Mazda is designed to blur the lines of conventional vehicle 
segmentation, offering traditional C-segment customers more in terms of design, 
craftsmanship and interior packaging, as well as a dynamic driving experience 
often associated with European-built vehicles. Its recent products have rewarded 
Mazda with a huge leap in sales. The company hopes the same formula will work 
for the Mazda3. <br>
<br>
The Mazda3 will be competing with a whole gang of 'small' (but getting larger) 
rivals, including the class-leading Toyota Corolla, Nissan Sunny, Hyundai 
Elantra and the Ford Ikon. It's a tough marketplace or is it? Still, Mazda has 
high hopes for its latest entrant, with project leader Akira Tanioka announcing 
that it was benchmarked against the VW Golf in the design and development stage, 
the aim being not necessarily to replace the Golf, but at least offer excellent 
dynamics and quality to those who couldn't afford one. <br>
<br>
For all practical purposes, the all-new Mazda3 is a global car, sharing 
technology and components with the upcoming Volvo S40. It's like a talent co-op. 
Purists may believe that component-sharing makes cars all the same, but it isn't 
necessarily so. Mazda developed the engines and transmissions, Volvo did the 
chassis and safety, and Ford did the suspension design. The suspension tuning 
was back in Mazda's hands, worked out at their rolling Hiroshima test track. 
Each manufacturer did what it does best, and the result is the best of three 
worlds.<br>
<br>
<b>Exteriors</b><br>
The new Mazda3 is a highly distinctive-looking sedan with Mazda's characteristic 
front-end featuring a bulging bonnet, sculpted guards, a five-point black 
honeycomb front grille and a stylish roof antenna. It has a sweet, rounded shape 
and a smooth, cohesive design akin to the Mazda6. It is taller than the old 323 
(and I suspect even a little higher than the Mazda6) and sits on a longer 
wheelbase, giving more interior space. <br>
<br>
The trademark wedge of a grille has horizontal bars. The headlamps have a sleek 
shape, swept back like a cat and sparkling with three beams inside. The whole 
front bumper, including the dam at the bottom with foglights at the corner, is 
impressively one piece. There's a small seam on each fender between the 
headlights and the wheel opening, and between the headlights there's only the 
hood crack. Everything south of that is one smooth and effective piece. <br>
<br>
The rear of the sedan is another smooth cohesive design with the integrated 
bumper, and again only small seams at the edge. The deck is short and high and 
nicely softened at the top edge. At all four corners, the wheelwells fit more 
tightly around the tyres. There used to be a rule at Mazda that there had to be 
enough of a wheelwell gap to install tyre chains without removing the tyre, 
which the stylists hated and finally defeated with the Mazda3. <br>
<br>
The Mazda3 is a couple of inches longer, wider and taller than the old 323. A 
lot of work went into the new unibody chassis, which is supposedly 40 per cent 
more rigid than the 323. In a nutshell, Mazda had a high styling standard for 
this car. It is available in 12 colours, some of which look almost metal-flake, 
for example the new passion orange mica and the sherbet green mica. </font></p>

<p align="justify"><font face="Verdana" size="1"><b>Interiors</b><br>
<img border="0" src="im7.jpg" align="right" hspace="5" width="200" height="200">If the Mazda3's exterior has a strong family resemblance to the 6 and 2, then 
the interior is even more so. The centre console has a simple stack of controls 
mainly with rotary knobs, topped off by an LCD display strip for air 
conditioning, stereo and other settings. Instruments in front of the driver are 
housed in three distinctly circular binnacles much like recent Alfa Romeos, and 
the round swivelling air vents reinforce the Italian ambience. No matter the 
provenance, it all looks rather good. <br>
<br>
The standard cloth interior is sturdy and the seats have adequate bolstering. 
Power seats aren't available, but who needs them in a compact car? Besides, 
they're what Mazda calls 'Neck Injury Minimising' seats. There is adjustable 
lumbar support, and the hip position is elevated, reducing front legroom a 
touch, but providing excellent forward visibility as well as a very tidy 
relationship with the pedals and especially the short shift lever. There's no 
dead pedal, but the outside of the driver's right shin rests comfortably on the 
edge of the centre stack. <br>
<br>
The three-spoke leather steering wheel feels great, and the control buttons 
(sound system) have a positive feel as well. There's an attractive faux carbon 
fibre horizontal strip on the panel, while the dashboard shelf is golf ball 
grainy. There is plenty of cabin width and height thanks mainly to the body's 
generous exterior dimensions, but a real feature is space in the rear seat. 
Legroom is generous even for bigger Omanis and there's space for a decent-sized 
bounce under the roof-lining as well.<br>
<br>
The glovebox is not only huge (8-litre, 260 mm), but the door is also dampened 
and it has its own light. There's a deep but not long console under the driver's 
right elbow, and between the seats are two built-in cup-holders with a neatly 
hinged cover in black plastic. The cup-holders have a canal between them so 
other things such as a cellphone can be stored and easily reached there.</font></p>

<p align="justify"><font face="Verdana" size="1"><b>Performance</b><br>
At first, the 1.6-litre MZR engine seems underpowered for a body as heavy as 
that of the Mazda3 (kerb weight: 1,250 kg), but run the engine through its 
rev-range with plenty of throttle (zoom-zoom a bit, if you please), and it 
responds well, producing a decent turn of speed and hiding the fact that this 
Mazda3 is about 50 kg heavier than the equivalent 323 model. The peaky torque 
delivery shows through in a relative lack of oomph in the lower and middle 
ranges of the rev-scale. <br>
<br>
The DOHC 16-valve engine is quite sophisticated, with variable valve timing and 
a variable induction system which optimises intake efficiency and torque. The 
block is aluminium, there's a cam chain rather than a belt, and the exhaust 
manifold is stainless steel. It's very smooth and quiet at consistent highway 
speeds, and has a nice sporty sound when the engine's revving under 
acceleration.<br>
<br>
Although the engine comes with relatively satisfactory spirit (107 hp/ 6,000 
rpm), it is, after all, a non-turbocharged four-cylinder, so there's not a ton 
of torque (14.5 kg-m/4,000 rpm). Be prepared to downshift to accelerate 
suddenly, either with the manual five-speed gearbox or the four-speed 
Activematic. Mazda calls the optional automatic transmission Activematic. You 
can just put it in &quot;Drive&quot; and go, but it features a manual mode programmed for 
quick shifting, making the Mazda3 the only car in its class offering such a 
transmission. It's also programmed to maintain its gear going downhill for slope 
control (engine braking), and uphill to reduce hunting.<br>
<br>
Mazda has worked on instilling its latest range of cars with fine handling 
response, and the 3 doesn't disappoint in that regard. There's plenty of 
front-end grip, with all but the 1.6V Sport variant getting 195/65 R15 tyres 
(steel rims with full wheel caps for the base and 1.6S Comfort models and alloy 
wheels for 1.6V Elegant trim). The Sport trim gets even lower-profile 205 R16 
alloy wheels. Electro-hydraulic power steering has just the right amounts of 
assistance and feedback.<br>
<br>
The standard five-speed manual shifts beautifully, especially the upshifts, 
which were almost as smooth as an automatic, with no real driver effort. This is 
the result of the redesigned synchronisers and cable linkage for reduced 
friction. Handling is quick and nimble, making the Mazda3 fun to drive. On 
twisty roads, the Mazda3 is fun to handle and it's sharp, true and steady in 
emergency lane-change manoeuvres. The Ford group in England designed the 
suspension (Front: MacPherson Struts; Rear: E-type Multi-link), but it was tuned 
by Mazda at its test track at Hiroshima.</font></p>

<p align="justify"><font face="Verdana" size="1"><b>Safety</b><br>
As usual, Mazda claims its new baby is stiffer and stronger than its predecessor 
with increased rigidity and crashworthiness thanks to MAIDAS - a system of 
dispersing crash impact energy through parts of the body frame, thus minimising 
cabin deformation. In more friendly terms, in the eventuality of a head-on 
collision, the front of the chassis is designed to redirect energy to the 
outside rails, and not down the centre toward the front seats. The steering 
column is crushable and the pedals are designed to retract away from the 
driver's feet. <br>
<br>
Other safety features include dual-stage SRS driver and passenger airbags, 
starter interlock safety system, ISOFIX child seat anchors (rear seat), front 
seatbelt pre-tensioners with load limiters, three lap-sash seatbelts in the rear 
and three rear headrests, 4-sensor 4-channel anti-lock brakes with electronic 
brake-force distribution, brake intrusion safety pedal and a host of other 
features not common in this class of vehicles. We found the brakes to be quite 
effective and sensitive; a mere light touch on the brake pedal around town is 
nice. In fact, the whole braking system has been upgraded with better hoses, 
master cylinder and pads, plus wider tires. </font></p>

<p align="justify"><font face="Verdana" size="1"><b>The finishing line</b><br>
Mazda is rightly boastful about its new entry-level car because it offers far 
more than anyone might expect in this price class. It's solidly executed, with 
no flaws, at least not at first blush: reasonable engine, transmission, 
handling, brakes, comfort, packaging and looks. The main thing is that the 
Mazda3 seems to have the goods in terms of style, space, safety equipment and 
that important driving spark to see it ignite the compact segment in Oman.<i><br>
<br>
Road test courtesy Towell Auto Centre LLC, the sole dealers of Mazda in the 
Sultanate.</i></font></p>

