            <p align="justify"><b><font face="Verdana" size="2" color="#FF0000">
            Whither consumer protection?</font></b></p>

            <p align="justify"><b><font face="Verdana" size="1">
            <img border="0" src="im8.jpg" align="left" hspace="4" width="125" height="158">Consumer 
            loan practices prevalent in Oman may deserve a closer look since not 
            only they increase credit risks to the banks, but also increases the 
            borrower's burden, believes Ali Hassan Moosa</font></b></p>

            <p align="justify"><font face="Verdana" size="1">The consumer loan 
            phenomenon in the Sultanate is quite unusual compared to developed 
            economies. Historically, and for a wide verity of reasons, a large 
            number of Omanis have relied on consumer loans. In loans parlance, 
            'personal' and 'consumer' are interchangeable terms. Consumer or 
            personal loans are unsecured and, therefore, are riskier than other 
            types of secured lending such as mortgage loans.<br>
            <br>
            An average Omani may avail of a personal loan for a housing purpose, 
            to purchase a car, to purchase consumer items, to get married, for 
            holiday travel or for self or children's education. It seems that 
            consumer loans have tapped into the lifestyle of a large number of 
            people in this country. There are a number of reasons why consumer 
            loans are so popular:<br>
            <br>
            </font><font face="Marlett" size="1">h</font><font face="Verdana" size="1"> 
            The lack of a comprehensive and efficient public transport system 
            has resulted in a situation where most people require personal means 
            of transportation and, therefore, most Omani adults purchase a car 
            even though they may not always have the required money to do so.<br>
            </font><font face="Marlett" size="1">h</font><font face="Verdana" size="1"> 
            It has also been evident that certain people like to keep up 
            appearances. Instead of buying an affordable car that takes you from 
            point A to point B, some borrow more than needed to purchase fancy 
            cars.<br>
            </font><font face="Marlett" size="1">h</font><font face="Verdana" size="1"> 
            Due to erroneous cultural grounds in some tribal societies in Oman, 
            some people may need to spend huge amounts of money for marriage, 
            most of which is from personal loans.<br>
            </font><font face="Marlett" size="1">h</font><font face="Verdana" size="1"> 
            The lack of loan options such as loans specifically for educational 
            purposes shove people into personal loans, resulting in consumer 
            loan portfolios comprising of a veriety of purposes.<br>
            </font><font face="Marlett" size="1">h</font><font face="Verdana" size="1"> 
            Getting a personal loan in most instances is painless. All that is 
            required is a salary assignment to the bank from a decent employer. 
            Such convenience attracts people across the board.<br>
            <br>
            UnmistakEably, due to the extraordinary demand for consumer loans, 
            banks made commercial decisions to enhance their profitability but, 
            unfortunately, may have exploited the borrowers in the process. 
            Commercial banks in the Sultanate shy away from offering 
            alternatives such as loans for housing and education. The main 
            reason behind this stance is referred to as 'cannibalisation'. Since 
            other types of loans may have to be offered at lower rates, a 
            commercial bank would impact its own profitability by offering 
            alternative loans.<br>
            <br>
            It is, indeed, worrying to note the excessive salary multiples based 
            on which banks grant personal loans. The proper method for 
            calculating loan amount eligibility is founded upon affordability 
            rather than the local practice of salary multiples. During the loan 
            application process, a borrower needs to be assessed in terms of 
            lifestyle and disposable income. Debt burden calculation (i.e., 
            affordability) takes into account the maximum ratio of loan 
            instalment to disposable income, and then the maximum loan amount is 
            derived.<br>
            <br>
            The lower the debt burden ratio, the safer it is for the institution 
            to lend. Using this approach, consumers are also protected against 
            unwarranted borrowing that may affect their ability to maintain 
            their existing and future generation lifestyles. <br>
            <br>
            Moreover, the method in which interest rate is calculated and 
            presented to the public varies from one financial institution to 
            another. Banks normally calculate interest rate based on reducing 
            balance - i.e., interest charged is on the outstanding loan balance. 
            Leasing companies calculate interest rate using flat rate method - 
            i.e., interest charged for the entire period is based on the 
            original loan amount.<br>
            <br>
            It is imperative that interest rate presented to the public is 
            founded upon a unified approach. A layperson may not understand the 
            difference in interest calculation between two quotes of a flat 
            interest rate of 6 per cent and a reducing balance interest rate of 
            10 per cent. The borrower may think that the former quote is cheaper 
            whereas, in reality, a 10 per cent reducing balance is cheaper than 
            a 6 per cent flat rate.<br>
            <br>
            Furthermore, even where interest rates are based on reducing 
            balance, banks adopt several approaches within the method. Locally, 
            most banks use compounded interest method where monthly interest is 
            added on top of the outstanding loan balance and then monthly 
            instalment is deducted. The customer ends up paying interest on 
            interest for no apparent reason. In some countries such as the US, 
            compound interest calculated as above is considered not only 
            unethical, but may also be classified as illegal.<br>
            <br>
            Despite the falling interest rate regime witnessed in the past few 
            years, it is disappointing to note that a number of banks have 
            proactively reduced interest rates on deposits but have only reduced 
            interest rates on consumer loans as a result of regulatory 
            intervention.<br>
            Charges imposed upon the borrower may sometimes be cumbersome. Loan 
            charges may include application fee, arrangement fee and pre-payment 
            fee. All charges must be transparent and interest rate calculation 
            needs to include all charges to assist borrowers in deciding which 
            bank offers the best deal. <br>
            <br>
            Internationally, a uniform approach for quoting interest rates 
            including charges is maintained. A popular method is Annual 
            Percentage Rate (APR) that amortises embedded loan charges along 
            with the interest rate. Elsewhere, due to their inherent risks, 
            consumer loans are granted only over short-term basis. In 
            comparison, similar loans are given locally over longer than usual 
            tenors ranging from four to five years. Furthermore, loan waivers 
            unduly extend loans to unacceptable tenors. Such practices not only 
            increase credit risks to the banks, but also increase borrowers' 
            burden.<br>
            <br>
            To protect banks and borrowers alike, banks need to stop the waiver 
            mania altogether (some banks grant up to three waivers per annum!), 
            or build in the waivers within the short-term loan - i.e., to give a 
            choice to the borrower to have no waivers or to increase the monthly 
            repayments and have waivers without extending the loan term.<br>
            <br>
            Banks capitalise on the high demand for consumer loans to generate 
            other income, mainly insurance commission. However, practices 
            endorsed by a number of banks here in the Sultanate are 
            questionable. For instance, at times, customers are not given the 
            choice of what type of cover they need. Furthermore, customers are 
            not given an option to 'shop around' for value-for-money insurance 
            coverage.<br>
            <br>
            Life insurance premium for personal loans is charged in advanced, 
            mostly by debiting the loan amount. If the borrower pre-pays the 
            loan, the insurance coverage is lost. To be fair to the customer, 
            banks and insurers could agree to provide the borrower with an 
            insurance credit note that can be utilised sometime in future. <br>
            <br>
            A bank may force a customer to accept an insurance cover that is 
            suitable to the bank (and not the customer). Furthermore, the bank 
            may be at liberty to charge whatever commission it sees fit on top 
            of the actual underwriting insurance premium. Some local banks 
            account approximately one-third of the insurance premium charged to 
            the borrower as insurance income.<br>
            <br>
            It is perfectly alright for banks to charge a commission for 
            arranging and administering the insurance coverage. Some of the 
            methods described above, however, suggest that the vast majority of 
            retail loan customers get an unfair deal with respect to insurance 
            coverage. Such banks must be urged to review insurance practices, 
            and if they choose to continue applying unjust methods, then it may 
            be appropriate for regulators to intervene.<br>
            <br>
            The local banking regulator, the Central Bank of Oman (CBO), does 
            have consumer protection as part of its agenda. The CBO does 
            investigate serious customer complaints and follows up with the 
            concerned institutions in order to amicably resolve matters, of 
            course, within the governing legal framework. The approach is, 
            indeed, encouraging and is much more than what many other regulators 
            across the world do. Furthermore, bank practices are regularly 
            examined by the CBO and corrective measures are initiated where 
            appropriate. <br>
            <br>
            It is, however, debatable whether the regulator should also act as a 
            watchdog. The two dissimilar missions of regulation and consumer 
            protection are segregated in Western economies for reasons of better 
            resource utilisation, efficiency and effectiveness.<br>
            <br>
            It may be worth considering the option of establishing an 
            independent self-governing watchdog in Oman charged solely with 
            consumer protection within the financial industry. The watchdog need 
            not only have the muscle as to influence financial intermediaries to 
            adopt internationally accepted best practices, but also access to 
            mass media in order to educate the public. Banks and other financial 
            institutions ought to be more responsible corporate citizens and be 
            proactive in this regard as to collectively accelerate the set-up of 
            a consumer protection watchdog.<br>
            <br>
