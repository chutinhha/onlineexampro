            <p align="justify"><font face="Verdana" size="1">
            <b>Value (f)or money?</b><br>
            While the success of no-frills airlines appears to support the 
            airlines' claims that consumers' choice of carrier is, first and 
            foremost, price-led, there are other criteria - transfer cost/times, 
            service quality and distribution - on which passengers judge an 
            airline. &quot;Our crew is trained like any other. Although onboard meals 
            come at a price, in terms of convenience and legroom, passengers 
            benefit from a large seat pitch of 34 inches compared to 31 inches 
            in regular airlines. Our A320 is custom-made to accommodate 150 
            seats/passengers instead of the usual 165. So, we make sure that we 
            do not comprise on comfort,&quot; defends Air Arabia's Sidhu.<br>
            <br>
            It would not be surprising to see the regular airlines initiating 
            similar services, like the Gulf Traveller, an extension of Gulf Air. 
            However, as a regular passenger puts it: &quot;The Gulf Traveller is 
            still as expensive as Oman Air or Gulf Air. If I can pocket half the 
            money which I spend on any of these airlines, why shouldn't I? In 
            fact, even packages like Muscat-Sharjah-Muscat work out well as it 
            includes airfare plus a two-night stay plus complementary breakfast 
            in addition to return airport transfers! And all this, again, in 
            half the regular airline tariff.&quot;</font></p>
            <p align="justify"><font face="Verdana" size="1">
            <b>Boom or bust?</b><br>
            That brings us to the million-riyal question - will the low-cost 
            model work in this region? Surprisingly, the answer comes from Oman 
            Air's Busaidy: &quot;The low-cost model has been successful for more than 
            a decade. As long as low-cost airlines do not deviate significantly 
            from the fundamentals of the successful business model, they should 
            be around for a long time. They certainly have gained popularity 
            among passengers, investors and airports.&quot;<br>
            <br>
            However, he is quick to add a word of caution: &quot;In the Gulf area, 
            low-cost carriers will, at least initially, face a significant 
            challenge in achieving the competitive advantages enjoyed by similar 
            airlines in other parts of the world due to a low population base, 
            low computer literacy limiting sales over the Internet, limited 
            number of secondary airports and restricted access to several key 
            markets surrounding the Gulf region.&quot;<br>
            <br>
            Busaidy expects the differences between the low-cost and full 
            service airlines to become narrower in the long run. &quot;Full-service 
            airlines are increasingly adopting the low-cost practice of 
            minimising sales from travel agents and sell more through the 
            Internet. On the other hand, low-cost carriers are increasingly 
            encroaching on major airports instead of only focusing on secondary 
            airports. Some full service airlines - e.g., Singapore Airlines, 
            United Airlines, and Virgin Atlantic - have gone to the extent of 
            creating their own low-cost subsidiaries to combat the threat of 
            low-cost carriers and to capitalise on new growth opportunities.&quot;
            <br>
            <br>
            No-frills or full-service airlines, whoever wins the battle for 
            supremacy in the Gulf skies, the consumer will eventually gain from 
            increased competition, lower airfares and better options.</font></p>

