            Lowering the bar</font><p align="justify"><font face="Verdana" size="1">
            <b>Low-cost airlines are the latest buzzword in the region's 
            aviation industry. Are they an alternative to road travel? 
            Nowfaleena Ebrahim reports</b></font></p>
            <p align="justify"><font face="Verdana" size="1">
            <img border="0" src="im3.jpg" align="left" hspace="5" width="250" height="145">Consider 
            this. You log on to the Internet, book a flight from Muscat to 
            Dubai, make the payment by your credit card, and get a confirmation 
            number in return, which you print out on your desktop printer. Next 
            stop is the airport - on the day you have to travel, not before. No 
            waiting for the airline agent to issue you a ticket book, no 
            haggling about confirming the date or meal-type or the preference of 
            seat. You present that A4-sized proof to the airline's officials at 
            the airport, and get a boarding pass in return. It has no seat 
            numbers mentioned on it. As you board the aircraft, choose the best 
            available seat and park yourself. Order a la carte from the 
            in-flight menu and enjoy your journey. If you think this is a scene 
            from the future, think again. The future is here.<br>
            <br>
            Welcome to the world of low-cost, no-frills airlines. With the 
            launch of Air Arabia, billed as the regions first low-cost airline, 
            we in the Middle East are now witnessing a phenomenon that first 
            took the US' airlines industry by storm in the 1970s. Airlines like 
            Air Arabia might be following on the lines of EasyJet or Ryannair, 
            but the fact is, until now, the only other option to flying 
            expensive available to consumers in the Gulf was travelling by road. 
            And the annoying traffic jams at the national borders would 
            definitely make anyone rethink about a cheaper option of travelling 
            by road - especially when you compare the tariffs and the 
            convenience. </font></p>
            <p align="justify"><font face="Verdana" size="1">
            <b>Who's flying?</b><br>
            The USP of a low-cost airline is that it can get you from City A to 
            City B in the same time as a full-service airline will but at half 
            or sometimes even lower cost. This makes it an enticing proposition 
            for passengers travelling by road. For instance, while a person in 
            Oman pays a total of RO20.500 (RO10 for a return bus ticket and 
            RO10.500 for the visa at the border) to travel to and back from 
            Dubai, s/he has to shell out a bit more (RO34) to fly Air Arabia 
            and, eventually, save precious time at the border. The catch, 
            though, is that the airline will take you to Sharjah - and not Dubai 
            - from where you will have to make your own arrangements to drive 
            down to Dubai. However, the airline has tried to offset that hitch 
            by providing a 96-hour 'free' visa-on-arrival, which is provided 
            exclusively for the airline's passengers. </font></p>
            <p align="center"><font face="Verdana" size="1">
            <img border="1" src="im25.jpg" width="298" height="129"></font></p>
            <p align="left"><font face="Verdana" size="1">
            So are low-cost airlines like Air Arabia a threat to full-service 
            airlines like Oman Air in the country? Answers Abdulrahaman Al 
            Busaidy, chief executive, Oman Air: &quot;Mostly, low-cost airlines focus 
            on different market segments than full-service airlines. They tend 
            to focus on price-conscious passengers who are willing to give up 
            certain conveniences, e.g., meals or flying to a secondary airport, 
            to obtain lower prices. Low-cost carriers tend to operate on a 
            point-to-point basis rather than connecting passengers through a 
            hub. On the other hand, full-service airlines pursue several market 
            segments, most notably, business traffic and flow traffic that 
            connect via a hub to other destinations. The requirements for 
            satisfying the various market segments are different and airlines 
            have adopted different business models to satisfy such 
            requirements.&quot;<br>
            <br>
            Busaidy is right. An executive used to the comforts and convenience 
            of business-class flying may not necessarily cross over to the 
            low-fare option while a weekend traveller or a holiday tourist, who 
            was earlier taking the journey by road, would get attracted by the 
            lower fares offered by a no-frills airline and decide to graduate to 
            air travel. As Busaidy puts it, &quot;Empirical evidence worldwide 
            indicates that low-cost carriers have created new traffic volumes 
            where they operate rather than simply take traffic from the 
            full-service airlines. In fact, they seem to divert traffic more 
            from surface transport rather than from other airlines.&quot;<br>
            <br>
            &quot;At this stage, Oman Air does not view low-cost carriers as a major 
            threat since Oman Air, as a full-service airline, focuses on 
            different market segments, viz. business traffic, flow traffic and 
            inbound tourist traffic to Oman. Moreover, Oman Air has reduced the 
            threat by diversifying into new long-haul markets - e.g., East 
            Africa - and entered into a strategic alliance with Gulf Air which 
            will extend Oman Air's reach to long-haul routes through 
            code-sharing on Gulf Air's flights. On the other hand, Oman Air is 
            vigilant on its cost structure; so, it can be competitive with the 
            low-cost carriers,&quot; adds Busaidy. </font></p>
            <p align="justify"><font face="Verdana" size="1">
            <b>Competition ahoy!</b><br>
            Air Arabia took to the skies at a time when speculations were rife 
            that MenaJet, an airline privately owned by the Al-Zamil Group of 
            Saudi Arabia and Gulf Finance House of Bahrain, was about to take 
            off in the first quarter of 2004. Unfortunately, it hasn't seen the 
            light of the day and, today, Air Arabia is the only bona fide 
            low-cost airline in the region with 14 destinations to its credit. 
            That the airline is the national carrier of the UAE (fully owned by 
            His Highness Dr Sheikh Sultan Bin Mohammed Al Qassimi, Member of the 
            Supreme Council and Ruler of Sharjah) and is backed by the 
            government helps too.<br>
            <br>
            MenaJet, despite the delay in its takeoff, holds its claim. Mazen 
            Hajjar, chief commercial officer, MenaJet, told OER: &quot;We are looking 
            for commencement of operations very soon. We do not face a threat 
            but rather we compete with many airlines and other modes of 
            transport. We will be offering passengers a new type of product. 
            Just because there is a Beetle, people do not stop buying a BMW!&quot;<br>
            <br>
            It may be recalled what Stelios Haji-Ioannou, who launched EasyJet 
            in 1995, recently said in an interview. He spoke about the logic of 
            a low-cost airline in the Middle East, which is highly regulated: 
            &quot;It is difficult. The pace of change will depend on the willingness 
            of governments to actually deregulate. Unless you begin to get 
            people flying for next to nothing, why should you start cheering for 
            deregulation that the government has no incentive to actually give?&quot; </font></p>
            <p align="justify"><font face="Verdana" size="1">
            <b>How cheap!</b><br>
            The no-frills product is based around online booking, a relatively 
            new way of distribution - one that is simple, quick and, most 
            importantly, cheap. These airlines rarely use travel agents (to 
            avoid paying out commissions) and actively steer passengers towards 
            online bookings and away from their call centres (to minimise cost 
            associated with staffing such centres). In addition, they do not use 
            tickets (and free themselves from the administration costs of 
            producing and accounting for them). Such practices are fundamental 
            to keeping fares low. Mike Sidhu, general manager, Shanfari Travel 
            Tourism LLC, the official agents for Air Arabia in Oman, affirms 
            that Air Arabia follows the online ticketing route. &quot;We are trying 
            to promote Internet-based ticketing so that we save up on agency and 
            distribution costs. It's also an education process - hence we also 
            have our Website address on the aircraft,&quot; he claims.<br>
            <br>
            While no-frills airlines are, indeed, less expensive than their 
            'fully-loaded' counterparts, it does not necessarily mean that they 
            offer the best value for money. These carriers often serve airports 
            that are further from the city centre than those used by 
            full-service airlines - thus requiring additional money (and time) 
            to be spent on public transport or taxis getting to the city centre. 
            Most full-service airlines (and a few no-frills carriers) use 
            airports close to the city centre. &quot;We looked at the convenience of 
            passengers and found that Sharjah airport was the best bet. It's 
            close to Dubai and the crowd gets cleared fast. We have also started 
            inter-city coach services at a nominal cost,&quot; says Sidhu.<br>
            <br>
            Moreover, low-cost carriers tend to jack up their ticket prices as 
            departure date approaches. The earlier you book, the better your 
            chances of a bargain. Air Arabia, for one, sells the tickets on a 
            policy that if the passenger is sure to fly, he must buy the ticket 
            well in advance or else pay the price for the delay. So, while a 
            Muscat-Sharjah-Muscat ticket will cost you RO20.500 if you buy one 
            well in advance, the airline can charge you RO70 in case you want to 
            catch a flight within a few hours. An interesting option that Air 
            Arabia provides is the fact that the ticket is 'transferable'. &quot;If 
            you cannot travel on your ticket, your colleague can,&quot; clarifies 
            Sidhu. But then, for every transaction and change regarding a booked 
            ticket, there's a penalty fee of RO5.</font></p>
