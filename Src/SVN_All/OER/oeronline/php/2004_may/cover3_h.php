            </font><font face="Verdana" size="4">CONSOLIDATION TIME</font></p>
            <p align="justify"><b><font face="Verdana" size="1">
            
            <font color="#000000">With the two-year-long corporate loan book 
            clean-up exercise having concluded last year, the worst may be over 
            for the Sultanate’s first fully Omani-owned commercial bank, which 
            celebrated its 20th anniversary in 2003</font></a></font></b><br>
            <br>
            <font face="Verdana" size="1">“We had a very good year,” exclaims 
            John Carlough, the CEO of Oman International<img border="0" src="joh.jpg" align="right" hspace="5" vspace="2" width="100" height="135"> Bank (OIB), when asked 
            about the performance of the bank in 2003. Let’s have a look at the 
            bank’s figures to see what they say. OIB’s operating profits saw a 
            decline of 8.3%, from RO16.86 million in 2002 to RO15.46 in 2003. 
            The bank’s interest income dipped 13.4%, from RO40.406 million in 
            2003 to RO34.974 million, and the earnings per share (EPS) fell 
            28.5%, from RO0.294 to RO0.210. Why, then, is this man smiling? 
            That’s because the worst may be over for OIB. It is no secret that 
            the bank has been caught up in a corporate book clean-up exercise 
            for the past two years – something that has had an impact on its 
            balance-sheet as well. That exercise, informs a beaming Carlough, 
            got over in&nbsp; the third quarter of 2003. And now, he says, is 
            the time to consolidate the progress made during that time. “Not 
            only has the clean-up exercise been successful, we have had very 
            encouraging results in the first quarter of 2004, something that is 
            a direct consequence of our efforts in the 2002-03,” claims Carlough.
            <br>
            <br>
            <b>Growth matters</b><br>
            In the bank’s annual report 2003, Reem Omar
            <img border="0" src="infor7.jpg" align="left" hspace="5" vspace="2" width="200" height="338">Zawawi, 
            the bank’s chairperson, puts the decline in the bank’s operating 
            profits as ‘anticipated’, and attributes it to the bank’s decision 
            to implement stringent credit risk management principles to ensure a 
            significant improvement in asset quality. Central Bank of Oman (CBO) 
            regulations require provision for loan impairment be made on the 
            basis of higher of provision as per IAS 39 or as per CBO norms on an 
            individual account-byaccount basis. Accordingly, and as directed by 
            the CBO, provision made during the year for loan impairment is 
            higher by OMR 944,000 (2002 – Nil) compared to IAS 39 requirements. 
            Zawawi states that the bank’s “success of recovering several major 
            non-performing loans totalling RO13.5 million resulted in a bottom 
            line impact of RO5.70 million. Accordingly, the bank achieved a net 
            profit of RO13.22 million.” As far as the bank’s shareholders are 
            concerned, they have had a good 2003. Not only did the bank’s board 
            propose a cash dividend of 21% to be paid in 2004 in view of the 
            total profit available for distribution being 30.1% of the share 
            capital, OIB’s share price also appreciated by 53.9% during the 
            year, from RO1.540 as at December 31, 2002 to RO2.370 as at December 
            31, 2003.</font> <br>
            <br>
            <font face="Verdana" size="1">In 2004, informs Carlough, the bank 
            will continue to grow the books as well as its people’s skill-sets 
            in order to achieve its objective of becoming the No. 1 bank in the 
            country. “The bank’s focus in 2004 will actually be on both the 
            retail as well as the corporate sides. We are primarily a retail 
            bank and have a high service level. We will continue to be creative 
            with our Mandoos and other schemes,” maintains Carlough, who 
            believes that such schemes help the bank in attracting a lot of 
            retail customers, a fact that is backed by market share statistics.<br>
            <br>
            “On the other hand, we need to generate some additional activity on 
            the corporate side. We are the only transactional banking service in 
            the country, and it makes managing our core corporate customers much 
            easier. It is labour-free, electronic and economical for us to 
            service more of our corporate clients with additional services. We 
            have hired new people in our corporate banking division, and hope to 
            do well in it,” he adds.<br>
            <br>
            <b>Business scope</b><br>
            Last year when OER met Carlough, he had informed us that the bank 
            was in the process of rethinking its private banking business. That 
            rethink, states Carlough, is now through and the bank will be soon 
            rolling out a new service aimed at the retail upmarket customers. 
            “Recently, we have purchased new software that will allow us to go 
            into the brokerage business. And probably by the end of this year or 
            the first quarter of 2005, we will have new private banking products 
            as well.”<br>
            <br>
            In the short term, the bank is planning to capitalise on the 
            opportunities in the corporate market and cross-border lending. The 
            strategy of focusing on improved credit quality led<br>
            the bank to exit a number of relationships in 203 that did not meet 
            its credit standards. Accordingly, the bank’s loans and advances 
            stood at RO388.02 million as at December 31, 2003 compared to 
            RO403.96 million as at December 31, 2002. “We know who we want to 
            lend to,” claims Carlough.<br>
            <br>
            On the retail side, however, the bank’s total deposits showed&nbsp; 
            a marginal increase of 3%, from RO458.90 million as at December 31, 
            2002 to RO472.73 million as at December 31, 2003. As a result of its 
            conscious policy to improve net interest margin, the bank’s low cost 
            deposits demonstrated an increase of 16.8% while high cost deposits 
            declined by 24.2% during the year.<br>
            <br>
            Internationally, the bank’s strategy in India is to focus on 
            recovery of problem loans, limit asset growth, improve cost of funds 
            and reduce operating costs, declares Carlough. These initiatives had 
            a positive impact on the bank’s performance in 2003, with the net 
            loss from its Indian operations decreasing by 42% year-on-year. The 
            operations in Pakistan continue on a stable footing, and the bank’s 
            unit there returned marginal profits in 2003after a loan-book 
            clean-up exercise.<br>
            <br>
            <b>Tech edge</b><br>
            Carlough maintains that OIB has set the pace in terms of adopting 
            and adapting technology in the banking sector, and that it is the 
            market leader in this regard owing to a lot of firsts to its 
            credits. “The important thing is to not only have the technology, 
            but to know how and, most importantly, when to use it,” he explains. 
            “We made significant technological purchases, including software 
            packages for e-banking services, last year. The Phase I of this 
            project has already been implemented.”<br>
            <br>
            According to Carlough, OIB plans to implement new technology in a 
            rational way. “We are at present working towards increasing activity 
            with current technology.” Carlough revealed that the bank has plans 
            for and has even budgeted for providing SMS banking services to its 
            customers.<br>
            <br>
            <b>Corporate commitment</b><br>
            The bank’s commitment to corporate social responsibility extended 
            across Oman through providing sponsorship to a large number of 
            programmes and cultural events. These included the Oman Award for 
            Excellence, the Muscat International Book Fair, the GCC 
            Industrialists Conference, Muscat Festival, the 2003 General Census 
            and, more recently, the ICT Conference 2004. “We have always 
            believed in being a good corporate citizen,” affirms Carlough. “The 
            bank’s commitment is based on its desire to maintain the highest 
            standards of social awareness in its business activities and to 
            actively support education, social and other culture activities 
            within the community,” he adds.<br>
            <br>
            <b>HR and Omanisation</b><br>
            “The bank strongly believes in HR development and is committed to 
            continuing the improvement in the bank’s level of Omanisation,” 
            avows Carlough. “A lot of progress has been made in the rightsizing 
            exercise undertaken by the bank during the last year, and we have 
            managed to improve organizational productivity,” he informs. 
            Besides, major training initiatives were undertaken during the 
            course of the year to meet the bank’s staff development objective.<br>
            <br>
            OIB collaborated with two leading international training institutes 
            to provide training in both technical and banking subjects to its 
            staff. A total of 154 training sessions were conducted during the 
            year, he reveals. Training was focused on Omani employees, with 
            99.2% of the total Omani staff of 834 attending at least one 
            training course during the year. At the year end, OIB’s Omanisation 
            level of 91.75% was in excess of the CBO target of 90%. “We will 
            continue to keep our Omanisation levels in the range of 91-92%,” 
            declares Carlough.</font></p>
