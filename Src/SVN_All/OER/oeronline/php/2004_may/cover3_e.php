                    Cover Story</font><p align="center"><b><font size="2" color="#FF0000" face="Verdana">OMAN 
            OIL MARKETING CO.</font></b><font size="4" color="#FF0000" face="Verdana"><br>
            </font><font face="Verdana" size="4">BRAND NEW IDENTITY</font></p>
            <p align="justify" dir="ltr"><font face="Verdana" size="1"><b>Oman 
            Oil Marketing Company (formerly BP Oman) has focused much of its 
            attention on launching a newcorporate identity. While the resulting 
            ‘omanoil’ brand is a dramatic transformation, the 
            company’sperformance in 2003 remains more or less unchanged<br>
            <br>
            </b>One year ago, OER exclusively outlined many of the processes 
            which were getting<img border="0" src="mic.jpg" align="right" hspace="5" vspace="2" width="100" height="138"> 
            underway as a new corporate identity was being developed for Oman 
            Oil Marketing Co. SAOG (OOMC), the erstwhile BP Oman which was 
            forced to change its name and ‘Articles of Association’ when BP 
            decided to part company at the end of 2002. At the time of going to 
            press, the transition has been completed on time with OOMC 
            successfully re-branding every one of its 79 ‘omanoil’ filling 
            stations to the satisfaction of the company’s new majority 
            shareholder Oman Oil Company SAOC. And as promised last year by 
            David Douglas, chairman of OOMC, the process of transition hasn’t 
            had a negative impact on the company. All the capital costs of 
            re-branding and the transition from BP including market research, 
            brand design and legal costs are factored into the results of 2003. 
            At the same time, the old BP signage and other paraphernalia, which 
            have been discarded, have been written off. Given the change in 
            identity and the overall reduction in volumes from the commercial 
            and aviation sectors, the company’s focus on “operational 
            efficiencies with cost containment” together with a oneoff tax 
            windfall has ensured another profitable fiscal performance for 2003.<b>&nbsp;
            <br>
            <br>
            </b>“In its fifth year of operation as a public company, t<b><img border="0" src="infor5.jpg" align="left" vspace="2" hspace="5" width="200" height="318"></b>he 
            profit before tax results were in line with our forecast and the 
            after tax results have been significantly increased with a tax 
            provision write back being enjoyed. Even more pleasing is that this 
            performance has been achieved in an increasingly competitive market 
            and while we were going through the process of separating our 
            operations from BP,” remarks Douglas. “The transition from BP has 
            been extremely challenging but the rebranding has created a new 
            sense of excitement within the company and we believe within our 
            customer base.”<br>
            <br>
            Despite a reduction in turnover of around RO7 million reflecting 
            lower volumes of fuel sold, the pre-tax tax profit for 2003 was 
            RO2.2 million and after tax was RO2.6 million. “The balance sheet 
            remains strong with debtors receiving added attention that resulted 
            in a reduction close to RO1 million,” he adds.<br>
            <br>
            “The continuing focus on business improvements and new opportunities 
            has led to aggressive capital expenditure spending of RO1.7 million 
            thereby increasing the asset base. This has resulted in a net asset 
            value per share increase to RO1.893 from RO1.737 the previous year.”<br>
            <br>
            Following the achievements of 2003, OOMC recommended a cash dividend 
            of RO0.260 per share in line with the company’s policy of 
            maintaining a consistent dividend policy.<br>
            <br>
            <b>Retail reckoning</b><br>
            2003 was very challenging for the fuel retail sector. Nevertheless, 
            OOMC managed to build 4 new filling stations in strategic locations 
            (the company’s first new sites in several years), raze and rebuild 
            one of its major sites and identify a large number of potential 
            areas for development. “In 2004, our company is set to build more 
            sites than we built last year,” says Michael G. Wilson, managing 
            director, OOMC, in an exclusive interview with OER. “Ultimately, our 
            retail performance will be dependent upon the level of customer 
            service we offer, our brand image and the control of our cost base.” 
            Aside from the re- maging of all the sites to ‘omanoil’, other 
            retail initiatives last year included the launch of 6 further 
            convenience shops as well as a cross acceptance fuel card agreement 
            between OOMC and Emirates General Petroleum Corp. (Emarats) of Dubai 
            making it easier for regional custom to be generated. OOMC was the 
            first Omani oil company to offer customers the facility to use a 
            common card both here and the UAE. A cross border agreement between 
            local rival Al Maha with Emirates National Oil Company (ENOC) LLC, 
            and its subsidiary Emirates Petroleum Products Company (EPPCO) LLC 
            has since followed OOMC’s pioneering alliance.<br>
            <br>
            <b>Air traffic controlled?</b><br>
            The aviation business for OOMC failed to meet both its volume and 
            contribution targets due to a reduction in regional airlines 
            refuelling out of Seeb International Airport. This position is 
            worsened by the failure of Oman to price its products competitively. 
            Although the pricing mechanism is moving towards the same format as 
            those used by regional airports in Dubai, Abu Dhabi and Manama, the 
            fact that pricing becomes easily comparable may need to be supported 
            with cheaper fuel to help make the airport’s fuel more attractive 
            for uplift to regional airlines. Meanwhile, the fact that OOMC has 
            won the supply contract for the Royal Air Force of Oman (RAFO) has 
            partially compensated for the reduction of volumes from the airport 
            but military traffic has tapered off somewhat since the beginning of 
            2003. Wilson notes: “OOMC has recently signed a Technical Support 
            agreement with Air BP which enables the company to gain affordable 
            insurance coverage. Additionally, BP audits<br>
            <br>
            our control systems on safety and other performance criteria to 
            ensure international standards are maintained. The arrangement 
            allows us to keep abreast of the latest processes and equipment 
            being used in the industry,” Meanwhile, a Commercial Agreement 
            enables the company to market the refuelling services of BP Air at 
            Seeb International Airport. Both agreements have 5-year terms. “In 
            addition, our company has secured a distribution agreement with BP 
            and Castrol to be the sole representative for both brands of 
            lubricants.” The lubricants sector contributes much less revenue 
            than retail and aviation for OOMC, but the continued marketing of 
            these products was vital and will have a positive impact on future 
            business.<br>
            <br>
            <b>The future</b><br>
            2004 will probably not be as electrifying as last year for OOMC. 
            Like its counterpart Shell Oman Marketing Co. SAOG, the demand for 
            fuels from power stations sold to the Ministry of Electricity and 
            Water have evaporated. While the retail market is growing at around 
            5 to 6 per cent per annum, new sites are being built at around 11 
            per cent each year. Broadly speaking, the volumes per site will be 
            reduced which will make them less profitable.<br>
            <br>
&nbsp;“I’m not unduly pessimistic about 2004. Our company has so far had a 
            better-than-expected first quarter in 2004,” remarks Wilson who 
            concedes that the main challenge will be to conserve the level of 
            profitability that the company has maintained in the past. <br>
            <br>
            “I think our strength lies in our operational efficiencies and 
            flexibility. We’re a very different company from 12 months ago. We 
            are a stand-alone enterprise with our own IT and accounting 
            systems.” <br>
            <br>
            And with the re-branding successfully completed, OOMC can eye core 
            business revenue streams outside of Oman more vigorously as well as 
            related business activities within the country’s borders. “The 
            search has certainly begun and we have already had a few approaches. 
            Although it’s looking very interesting, the results of any proposed 
            expansion probably will not mature until next year onwards,” he 
            reveals. <br>
            <br>
            Clearly, the departure of BP as a shareholder has unleashed a brand 
            new scope of investment opportunities for OOMC. “Oman Oil Company is 
            willing to invest in our company’s development and we have a solid 
            base to provide the expertise in downstreammarketing ventures within 
            the region. As part of BP earlier, the company was restricted to 
            which countries it could compete in because the BP franchise was 
            represented&nbsp; elsewhere. Today, we do not have this 
            restriction.” <br>
            <br>
            Expanding into new areas of business outside Oman will not be easy. 
            It will be a stern test to find the right type of opportunities. 
            Douglas concludes: “We will continue to screen and evaluate 
            potential opportunities that we believe fit with the competencies of 
            the company and are confident that attractive ones will ultimately 
            be found. However, as with growth in our current businesses, the key 
            is to find opportunities to expand profitably.” <br>
