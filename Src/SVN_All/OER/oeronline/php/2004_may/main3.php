            During 2003, NBO also made a reasonably large provision of RO2.1 
            million for impairment on an amount due from a bank. This 
            represented about 80 per cent of its participation in a syndicated 
            loan by its Oman operations. Clean and fresh after the big bath, NBO 
            is probably set for a profitable year in 2004. In the Management 
            Discussion and Analysis Report included in the Annual Report for the 
            year 2003, the CEO of the bank forecasts sustained profitability 
            from the year 2004 onwards. The results of the year 2004 would also 
            be appropriate to judge the performance of the new management, as 
            this would have given time for their policies and strategies to take 
            effect. <br>
            <br>
            Sometimes, it is a change of industry fortunes that provides the 
            opportunity for a big bath. This happened in the telecom industry. 
            The global telecom industry started its downslide in 2001. Not 
            expecting this downturn, the industry had accumulated huge 
            inventories, extended significant credit and made acquisitions many 
            of which were highly overvalued resulting in worthless goodwill on 
            the books. The excuse of an industry recession led many players in 
            the market to make big provisions. Lucent Technologies provided for 
            about $1.74 billion (RO0.7 billion) for unrecoverable debtors and 
            impaired goodwill in 2002. JDS Uniphase wrote off about $40 million 
            (RO15.4 million) in impaired goodwill in 2001. Although most of 
            these write-offs were a result of the industry downturn, such 
            situations give management a chance to thoroughly clean up their 
            balance-sheets.</font></p>
