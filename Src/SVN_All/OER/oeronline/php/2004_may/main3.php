            profits of RO10.7 million in 1999 and RO8.3 million in 2000, the 
            bank recorded a loss of RO7.5 million in 2001. Mid-2002 saw the exit 
            of the chief executive officer. He could probably see the snowball 
            coming his way. The new CEO joined in November 2002. The bank 
            recorded a net loss of RO262,000 in 2002 and a whopping loss of 
            RO51.78 million in 2003. As the new CEO joined during the tail-end 
            of 2002, the full impact of the big bath has come in 2003. The 
