            <b>Have you had a big bath recently? It depends on whether you have 
            had a change in executive management, a merger or some other 
            structural change. A big bath is management jargon for a 
            balance-sheet clean out, explains Afshan Patel </b> </font></p>
            <p align="justify"><font face="Verdana" size="1">
            <img border="0" src="im1.jpg" align="left" width="100" height="133">When 
            a change in corporate structure takes place, it is an opportunity 
            for the management to take big write-offs or to make huge provisions 
            for impairment of assets. These assets may be obsolete machinery, 
            slow moving inventories, unrecoverable debtors or any other 
            unproductive or intangible assets. An executive management that has 
            been heading an organisation for some time has a tendency to drag 
            its feet when it comes to making substantial provisions especially 
            if they aren't strongly recommended by their audit process.<br>
            <br>
            But not so a new management, if the requirement for provisions can 
            be directly or indirectly attributed to a change in corporate 
            structure, which gives the management an opportunity to start with a 
            clean slate. Free from the deadweight of unproductive assets on the 
            books, the management gets another chance at lean operations that 
            reflect an efficient proactive organisation's capabilities.<br>
            <br>
            How do these unproductive assets get on the books in the first 
            place? In the course of business, an organisation would acquire 
            revenue-producing assets, hold inventory, extend credit, and 
            sometimes buy out other businesses. Estimating the useful life of 
            machinery, forecasting ideal levels of inventory holding and 
            assessing the creditworthiness of clients are integral business 
            decisions that involve an element of discretion and judgement. The 
            premises on which these business decisions are made sometimes don't 
            hold, and errors in judgement or changed circumstances result in 
            unproductive assets being carried on the books. <br>
            <br>
            <img border="0" src="im2.jpg" align="right" width="300" height="235">Every 
            organisation would, as an inherent risk of just being in business, 
            have to make certain provisions and write-offs against impairment of 
            assets. The mark of a well-managed organisation would be able to 
            keep its provisions lower than industry average. Requirements for 
            provisions may have a snowballing effect. The more you delay them, 
            the bigger the impact they are likely to have at a future date. In 
            some cases, the snowball turns into an avalanche and hits the 
            management real hard. This can result in a change of executive 
            management or encourage a merger or take over. <br>
            <br>
            If this happens, the new management is unlikely to pay for the sins 
            of its predecessor management. As part of the negotiations, the new 
            management is most likely to caution the board that its own 
            effectiveness and efficiency cannot be judged within the first or 
            second year of tenure. This will be the period of &quot;the big bath&quot;. 
            This will be the time for the new management to err on the side of 
            caution and make generous provisions for impairment in the values of 
            assets - maybe even provisions that they would not have made had 
            they been in business for say four to five years. With this 
            treatment, the blow to the bottom line is taken in one or two years, 
            instead of spreading the negative effects gradually and reporting 
            lacklustre results over an extended period of time.<br>
            <br>
            On the International scene, America On Line (AOL) and Time Warner 
            merged in January 2001. Richard D. Parsons took charge as CEO in May 
            2002 and took on the additional charge of chairman of the board in 
            May 2003. The 2002 financial statements for AOL Time Warner included 
            $100 billion (RO38.5 billion) in write-offs for goodwill and 
            overvalued assets. The net income from continuing operations of the 
            consolidated activities of the company for the year 2002 was a loss 
            of $43.4 billion (RO16.7 billion). The comparative figure for 2003 
            was a profit of $3.1 billion (RO1.2 billion) after the $100-billion 
            big bath in 2002. CEO Parsons is looking to 2004 with enthusiasm.<br>
            <br>
