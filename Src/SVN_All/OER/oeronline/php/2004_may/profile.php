                    potent of the weapons in his arsenal - his beliefs. &quot;The one 
                    thing that I have come to realise and firmly believe over 
                    the 35 years that I have been a banker and have seen the 
                    banking industry in Oman grow from one dominated by 
                    international players to one that is equally, if not more 
                    strongly, represented by local banks, is that there is 
                    really no substitute for hard work!&quot; says a visibly proud 
                    Issa. <br>
