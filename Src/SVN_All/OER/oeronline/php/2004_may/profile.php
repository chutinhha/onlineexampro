                    the bank to this position of glory, let's first look at the 
                    background of the man who made it all possible. On 
                    completion of his studies in the Sultanate of Oman in 1969, 
                    Issa started his career as a management trainee with 
                    Grindlay's Bank, where, according to him, he went on to gain 
                    considerable experience across the operations of the bank. 
                    &quot;Through the course of my career thereafter, I have had the 
                    opportunity to attend and be a part of numerous training 
                    programmes and professional courses including a Management 
                    Development Programme at Harvard University, Graduate School 
                    of Business Administration in Boston Massachusetts in 1994,&quot; 
                    he declares. <br>
