                    sense of empowerment. This cannot happen in a centralised 
                    operations structure. We are decentralised and yet promote 
                    open channels of communication and the free exchange of 
                    ideas so that everyone concerned is aware of all 
                    developments. This also allows the senior management the 
                    flexibility to step in and provide counsel, if it is 
