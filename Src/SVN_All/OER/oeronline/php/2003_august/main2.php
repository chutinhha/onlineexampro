            <font face="Verdana"><font size="4" color="#FF0000">Advantage Oman<br>
            <br>
            </font>
            <font size="1"><b>The Omani economy has greater advantage within the
            GCC region. It should, however, strengthen its trade position in the
            new customs union environment so as to carve a n</b><b>iche for
            itself, says Tom Farole&nbsp;</b><br>
            <br>
            <img border="0" src="adv_oman.jpg" align="left" hspace="5" vspace="5" width="300" height="298">Efforts to integrate the Gulf Cooperative Council (GCC) region reached a critical juncture in January this year with the establishment of a customs union, which is seen as a crucial step towards setting up a full-fledged Monetary Union by 2010. Increased efficiency as a result of a smoother flow of goods should help grease the wheels of trade although, on the whole, major driving forces behind the intra-GCC trade have been in place for the last couple of
            decades.<br>
            With the advent of the customs union, it is time to reflect on how trade in the GCC has developed over the years, assess the future of intra-regional trade and analyse how it is relevant to Oman's policy of economic
            development.</font></font></p>
            <p>
            <font size="1" face="Verdana"><b>Intra-GCC trade</b><br>
            Setting up a free trade area in the GCC was the first significant step towards the process of integration of the region's economies. According to the standard trade theory (exemplified in the development of the European Union), the GCC relied on free trade to help stimulate the development of complementary economic structures, with countries in the region specialising in various areas and enjoying comparative advantage.&nbsp;<br>
 For instance, Oman could become the primary centre for regional agricultural products, exporting fresh and processed food across the GCC, while importing certain manufactured goods from Bahrain, services from the UAE, and so
            on.<br>
            Although the free trade agreement has been in place for the last 20 years and business among the GCC countries has grown, intra-regional trade has fallen short of expectations. That is not to say that economic performance has been poor; on the contrary, Oman and many other GCC countries have made great strides towards economic development and achieved a global stature during this time. What is, perhaps, surprising is that this economic development has taken place in spite of limited success in trade
            specialisation.<br>
            If one looks at the recent trade data, one would notice that business across the GCC countries represents a mere six per cent of their total trade - inter-GCC trade is significantly less than their trade with other countries, such as the EU (17 per cent of the GCC trade), Japan (16 per cent) and the US (10 per cent). This looks dull in comparison to trade levels within other regional
            blocs.<br>
            Intra-regional trade in the GCC is relatively stable and has shown little growth over the past decade. What the consolidated figures mask is a significant variation across countries. The table below shows that Oman and Bahrain in particular have a significantly higher reliance on intra-GCC trade than other economies within the region. While this points to the more diversified nature of Omani and Bahraini economies, their trade relations are stronger with their
            'next door neighbours' (UAE and Oman; Saudi Arabia and Bahrain). This further proves that the specialisation of economies across the entire trade bloc is
            limited.<br>
            An explanation for the low levels of trade is relatively small size of the trading bloc. Although the six countries of the GCC combine to dominate the economy of the Arab world, in terms of population and GDP the region is similar in size to the Nordic region of Europe (Denmark, Finland, Norway and Sweden). However, even in the Nordic region, with the rest of the EU close by, intra-regional trade is more intense. For example, Sweden's trade with its three Nordic neighbours represents over 20 per cent of its total
            business.<br>
            A more compelling argument is continued reliance of the region's economy on oil. On the whole, the countries have not effectively diversified their economic bases. As a result, tradable goods and services in the region have been left aside. Finally, despite the free trade agreement, economic policies of the GCC countries have posed a number of barriers to efficient regional trade, including a lack of common external duties (until recently) and limited factor
            mobility.<br>
            In addition to these issues, one of the principal inhibitors to the development of intra-regional trade has been competition
            itself.</font></p>
            <p>
            <font size="1" face="Verdana"><b>Problem of competition</b><br>
            The level of competition between firms in the GCC is not a major problem in itself.       In fact, many trading sectors in the GCC are hypercompetitive. Instead, the problem of competition exists at a macro level. And the problem here is that the economic structures of the GCC countries are competitive rather than complementary. This problem is compounded following the intra-regional competition indulged in by governments of the GCC
            countries.</font></p>
            <p>
            <font size="1" face="Verdana"><b>Competitive structures</b><br>
            In theory, the more diverse the production structures of different partners in an economic bloc, the more potential there is for productive trade among them. Despite substantial efforts to diversify, remarkable similarity is noticed in the structures of GCC economies. A comparison of intra-industry trade indices (IIT), viewed as a key indicator of the degree of trade specialisation, shows that despite significant improvement in recent years, GCC economies have far less specialised industrial structures than most developed
            economies.<br>
            With a similar set of factor endowments (land, labour and capital) and similar economic
            'starting points', most countries in the region show identical trends in activities. This creates competitive economic structures and eliminates the potential for these countries to benefit from trade across the region. Further, the economies of the GCC have correlation between their trading patterns outside the region - i.e. they trade same types of products with the same countries. Little wonder then that governments of the region have embarked on an active policy of
            competition.</font></p>
            <p>
            <font size="1" face="Verdana"><b>Territorial competition</b><br>
            The massive scale of investment in the GCC provides it with a unique opportunity - to attract investment on the basis of comparative advantage, thereby catalysing the development of more complementary economic structures. The danger for the GCC is that too many governments in the region will follow the
            'me too' policy to attract investment and stimulate economic
            development.<br>
            Such a policy has a two-fold danger. First, it further compounds the problem of a lack of complementary economic structure. Secondly, when all regions are fighting for the same investment, the result is at best zero-sum at the GCC level: Dubai's win is Bahrain's loss, Oman's gain is Saudi Arabia's loss. In an increasingly integrated GCC, such a policy can be less
            tolerated.<br>
            Territorial competition is often a fundamental driver of sectoral strategy, with massive investments of government funds supporting the development of industries that are hardly based on specialised strategies. Consider the aviation sector for an example. In a region with a population of only 30 million, there are six national airlines (announcements have been made on new carriers in Sharjah and Abu Dhabi).</font></p>
            <p>
            <font size="1" face="Verdana"><b>Opportunities</b><br>
            In such a game of territorial competition, there will undoubtedly be both winners and losers. According to the European experience, winners tend to be the largest markets. In the GCC, it may mean Saudi Arabia, the UAE (specifically Dubai) and perhaps Qatar. Oman, along with Bahrain, will find itself in the toughest position when it comes to direct competition for investment. This makes it all the more important for these countries to vigorously seek out sources of sustainable competitive advantage and develop their
            economies.<br>
            Considering that there is a common set of natural endowments, shared culture and parallel economic development across the GCC, opportunities for specialisation are likely to come less from sectoral focus and more from horizontal, as well as vertical
            specialisation.<br>
            <b>. </b>Horizontal specialisation - Specialisation in different product/service groups within a sector (e.g. Oman is specialising in the production of a specific range of petroleum bi-products and linking up with other GCC countries to deliver a full range of chemical products);<br>
            <b>. </b>Vertical specialisation - Specialisation at different levels in production or the service value chain (e.g., running a call centre in Oman, while the physical fulfilment of orders takes place in
            Dubai).<br>
            With the small size of GCC markets, such specialisations will be critical to enabling the region to take advantage of the regional scale and develop products that can compete
            globally.<br>
            Finding ways to specialise is not an easy task. Gains from specialisation can, however, be significant. Ireland, for example, improved its per capita GDP from a mere 60 per cent of the EU average to over 110 per cent in two decades, thanks to a target-oriented economic development strategy and a competitive advantage that comprises English-speaking, skilled yet cheaper
            labour.<br>
            Identification of sources of potential competitive advantage in Oman should not be taken only at the macro-policy level, but should be addressed in detail at the sectoral level. Sectoral-level strategies that focus on defining sources of competitive advantage, understanding what it will take for Oman's businesses to leverage these sources and providing the necessary government support to ensure that these businesses have the tools they need to be successful, should be put in
            place.<br>
            While Oman has made great strides in diversifying its economy, increased focus on its key sources of a competitive advantage remains crucial to its development. A positive sign for Oman is that the government has avoided the
            'me too' attitude prevailing in the region and has followed a steady approach to economic policymaking. Recent developments, such as the Knowledge Oasis Muscat (KOM), illustrate its well-planned strategies to develop a sustainable competitive
            advantage.<br>
            The Omani economy does comprise quite a wide range in which it has greater advantage within the GCC region. It should strengthen its trade position in the new customs union environment. Such sources of advantage include:<br>
            <b>. </b>Agriculture - It including both land and marine resources. Oman already is a major provider of fresh foods to the UAE. However, there is tremendous potential to expand this trade to the entire GCC and move further downstream to expand the agro-processing industry.<br>
            <b>. </b>Ports - They provide an opportunity within the new customs union environment for Oman to become a more competitive platform for the region and to possibly go further to leverage this geographical advantage to become a base for regional production in specialised areas;<br>
            <b>. </b>Commodities - Apart from oil, Oman has a significant base of natural gas and minerals. This gives it an opportunity to specialise in downstream activities and related industries.<br>
            <b>. </b>Labour force - Not only the quality of its national skill base, but also its history of active participation in the labour force puts Oman in an advantageous position. This can be leveraged in many different areas, such as IT-enabled
            services.<br>
            The primary task for Oman as it works toward the Vision 2020 is to look within each of these areas (and others) to identify potential sources of a sustainable competitive advantage and concentrate on developing these opportunities. The dynamic growth of the GCC presents a great opportunity to Oman to leverage its potential to benefit from increased regional trade. Once it starts taking full advantage of its regional free trade area, a world of opportunities will open
            up.<br>
