 This forecasting failure throws up a number of probing questions. First, why did the analysts get it so wrong? Second - and far more important - will they eventually be proved
            right?<br>
            The first question is the easiest to answer. "The global oil environment remained one of potential shortage, as it was before the Iraq war, rather than potential surpluses, as predicted in the pre-war world," says a recent report by the Economist Intelligence Unit
            (EIU). "Prices have held up, inflated by a combination of Saudi Arabian production restraint, the slow return of Iraqi post-war return of Iraqi crude oil supplies has largely failed to materialise in the months of the war Looting has hindered Iraq's short-term production
