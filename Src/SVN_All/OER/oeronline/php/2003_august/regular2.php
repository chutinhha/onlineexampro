            On the second question, analysts at the EIU are confident that forecasts of an Iraq-inspired supply glut will come true - and sooner rather than later. Yes, there have been unforeseen delays in ramping Iraqi production up to pre-war levels, but these are temporary blips that will soon be ironed out. The EIU estimates that Iraqi production averaged 660,000 barrels per day in the second quarter of 2003, but that output will rise sharply to 1.5 million barrels in the third quarter and 2.1 million barrels per day in the fourth quarter.<br>
            "Important questions will emerge as Iraq slowly returns to the market in the second half of 2003 particularly as any growth in oil demand is forecast to remain relatively subdued," says the
            EIU. "Under our baseline scenario as Iraqi oil returns and pushes the market into heavy surplus, OPEC will be forced to take drastic supply-cutting action." This will fail, driving Brent Crude down to US$21.42 per barrel in the fourth quarter of this
            year.<br>
            What about 2004? The EIU sees the situation playing out like this: OPEC's initial efforts to restrain supplies in 2004 are destined to fail, as too many key producers will cheat on their existing quotas. However, this cheating will push the global oil market further into surplus in the second quarter of 2004, with an average daily surplus of more than three million barrels, driving the average second-quarter price of Brent Blend to US$16.14 per barrel.<br>
            "An oil price of US$15 per barrel would be catastrophic for most oil-producing nations, and this will force OPEC to make a determined effort to regain control of the market," says the
