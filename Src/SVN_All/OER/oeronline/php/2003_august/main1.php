            <p><font face="Verdana" size="4" color="#FF0000">Getting smart</font><font face="Verdana"><br>
            <br>
            </font><font face="Verdana" size="1"><b>The OAB Smart Card is mushrooming. Nayl D'Souza finds out what does the new electronic payment method have to offer to Oman's customers, retailers and the
            government</b><br>
            <br>
            <img border="0" src="OAB-Askalan.jpg" align="left" hspace="5" vspace="5" width="150" height="200">Smart card solutions have already begun to spark the public's imagination around the world and this trend is set to continue as consumers become increasingly convinced that it is a more beneficial, safe and fashionable alternative to making payments by cash or credit card. This new breed of payment is, as the name suggests, rather more clever than a credit card which is subject to a wave of security concerns - it is projected that card fraud losses could more than double in some economies by 2005. Smart cards that use a personal identification number (PIN), rather than scrawled signatures that few people check or that are not even required on Internet purchases, as proof of identity may spell the extinction of magnetic strip credit
            cards.<br>
            Locally, the application and utilisation of smart cards is gaining momentum thanks to an initiative launched six months ago to roll out the region's first active microprocessor offline payment system by Oman Arab Bank (OAB) SAOC, part of the Arab Bank Group PLC in Jordan. Today, the OAB Smart Card marketing campaign is in full swing as it becomes the de-facto e-payment system in the Sultanate.&nbsp;<br>
            "Oman Arab Bank has pioneered the concept of local e-payment for the government and the private sector here and has invested in the infrastructure required for such technology," says Abdul Kader Askalan, chief executive officer of OAB. As it continues to build this payment system here, it has effectively become a role model for banks across the Middle East and elsewhere to offer a fully customised payment service for their national markets.&nbsp;<br>
            <img border="0" src="smartcard.jpg" align="right" hspace="5" vspace="5" width="200" height="126">"The OAB Smart Card will provide Oman's government organisations with a very convenient and safe method of collecting fees and other types of government payments without the need to invest in infrastructure as it is already provided by OAB to all government organisations free of charge," he adds. In fact, the payment solution is available for companies and individuals who are non-OAB account holders as well as to existing customers of the
            bank.<br>
            OAB has become a financial services provider for the entire Omani market. The OAB Smart Card is already welcome at Oman Chamber of Commerce and Industry, the Ministry of Commerce and Industry, the Tender Board, Muscat Municipality, Sultan Centre, BP petrol stations, Al-Fair supermarkets, BP Express shops, Rawasco stores, Capital Store showrooms, and Philips electronics shops. OAB claims that many more retail stores are joining this new payment network on a daily basis. And while the bank will not reveal exactly how many smart cards have been issued, its popularity is unquestionably gaining strength as is evident from Mike Wilson, managing director of BP Oman, who says,
            "The benefits so far for BP Oman have included an increase in fuel sales at key locations where the OAB Smart Card was first accepted."&nbsp;<br>
 Yet, undoubtedly, the most scintillating aspect of this project from a business angle is that OAB enjoys a five-year exclusivity from the Ministry of Finance to offer the smart card payment service to clients in the Sultanate - a remarkable domestic banking coup that must be the envy of every other financial institution in the country. However, while there may be some jealousy, most local bankers convey their admiration for OAB's tenacity in sealing the deal and utilising such technology as a valuable addition to its existing retail and card product portfolio.&nbsp;</font></p>
            <p><font face="Verdana" size="1"><b>How it works</b><br>
            The new OAB Smart Card operates through the DUET (Direct Universal Electronic Transactions) technology platform for electronic payments. It enables every party participating in the payment system to enjoy significant benefits, but arguably its largest advantage is that the technology makes it possible to execute payment transactions directly whether in online or offline mode without requiring authentication from a third party.&nbsp;<br>
 DUET is based on performing transactions as a communication between two cards - the merchant's card and the customer's card. A dialogue between the cards is established either directly via a CAD (Card Acceptance Device) or on the Internet. Special session keys encrypt the transaction via the smart card and the system's back office to guarantee privacy, authentication, and security. The purchase amount is debited from the client's card and is credited to the merchant's card or directly to an account. At the same time the complete transaction data such as date and time, parties involved, amount paid, among other data is stored as an encrypted digital certificate in the memory of the card microprocessor.&nbsp;</font></p>
            <p><font face="Verdana" size="1"><b> Playing smart</b><br>
            Aside from those benefits already mentioned, Austria's BGS Smartcard Systems AG, a company which assisted OAB in installing the DUET system locally, insists its use will: speed up transactions and reduce time waiting for payments to be processed; reduce transactional and operational cost and lower cost of cash handling; increase revenue opportunities such as additional revenues from float accounts; provide knowledge of the customer's purchasing behaviour and habits; give a full audit trail and system integrity provided by the central management system; improve system reliability due to lack of external communication connections; allow easy settlements, provide better control of financial transactions for all parties involved; and reduce the cost of public services.&nbsp;</font></p>
            <p><font face="Verdana" size="1"><b>    Oman's advantage</b><br>
            Fledgling economies like Oman are considered to be at a significant advantage when it comes to implementing new e-payment solutions such as the OAB Smart Card because they are better equipped to leapfrog old technologies to implement today's solutions. In emerging economies like the Sultanate, most payments are usually in cash while the telecom sectors are usually going through rapid transition, substantial parts of the population are under banked and the threat of magnetic stripe card fraud is always a prevalent threat.&nbsp;<br>
 Moreover, current investments in less sophisticated payment technologies (magnetic stripe, e-purse and the like) are relatively negligible.&nbsp;<br>
  Conversely, developed economies may need to endure more of a painful transition to smart cards as considerable investment has been poured into point-of-sales machines that will need to be upgraded.&nbsp;<br>
 Retailers have told the banks in such markets that they do not believe they should be expected to subsidise the cost of the new technology required to make smart cards work at their tills. Meanwhile, customers fear that banks will use the smart cards as an excuse to increase the cost of processing credit card transactions.&nbsp;</font></p>
            <p><font face="Verdana" size="1"><b>   The new face of money</b><br>
            The multifunctional funds transfer system which OAB has deployed in the Sultanate is capable of handling numerous funds transfer scenarios be it retail payments, micro lending,
            micro-savings' collections, pension or welfare payments, social insurance benefits, loyalty transfers, or salary payments. Such applications pave the way for OAB to offer future functionalities soon, especially for the
            government.<br>
            While the bank admits that establishing an electronic payment system is a long-term process which requires establishing infrastructure to facilitate e-payments across a wide range of industries, Askalan hints that big changes are on the cards, particularly with regards spurring further e-government transactions.
            "E-government is comprised of many complex components that will take time and efforts to build. The OAB Smart Card is one of these many components and is a step in the right direction. As e-government projects come to life, the OAB Smart Card will evolve to address other forms of e-payments as needed," he
            says.<br>
            Being first to the party does not necessarily mean you are bound to have an enjoyable time but in this instance, OAB certainly is. And it is refreshing that Oman is at the forefront of this electronic revolution that others will follow.</font></p>
