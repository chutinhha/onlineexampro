            <img border="0" src="helm.jpg" align="left" hspace="5" vspace="5" width="194" height="225">Tell us about your career.</b><br>
            My career began with the completion of a sponsored business degree with Heinz. After about five years at the UK food giant, I joined the British Airports Authority (BAA) at Heathrow Airport. A few years later, I decided to join British Airways (BA) to learn more about the civil aviation business. From BA, I moved to International Aeradio, an organisation which had many interests in the Middle East and the Far East particularly with regard to the technical aspects of running airports. These included air traffic control, lighting, and communication systems. Then, in 1986, I had the opportunity to return to the BAA in a role that involved marketing and launching Stansted as London's third airport. While I was there, I was promoted to Marketing Director. Since then, I have stayed with the BAA although in 1992, I was offered the post of Managing Director of the London Tourism Board on secondment from BAA, to create a marketing strategy for London. This was an absolutely fabulous job which was concerned with creating the first ever brand for London which is still being used today. In 1994, on completion of the project I was offered the job of Managing Director of Southampton Airport where we succeeded in doubling the airport's size in five years and created a high quality businessmen's airport providing regular scheduled shuttle services to European destinations. Following on from this, I was asked to put my name forward for the position of chief executive at Oman Airports Management Company (OAMC) in Oman and started my role here in January
            2002.</font></p>

            <p>
            <font face="Verdana" size="1"><b>What has been the most rewarding challenge that you have
            overcome?</b><br>
            I think getting Stansted Airport off the ground during the recession in the UK was the largest and most rewarding challenge to date. Airports are very difficult to get started while they are small but once their size gets to a critical mass like Dubai Airport, they grow on their own steam. However, building such momentum is usually difficult. At Stansted Airport when I arrived there were only 500,000 passengers travelling through it but a new terminal was in the offing. We began marketing it aggressively and managed to convince Ryanair, a fledgling Irish budget airline which had ambitions to grow its low cost operations, to move there from Luton Airport. This was a major turning point which has meant that now over 15 million passengers each year travel at Stansted.&nbsp;</font></p>

            <p>
            <font face="Verdana" size="1"><b>  What is the strangest situation that you have experienced while at
            work?</b><br>
            The most bizarre experience I have had so far was at Heinz where I was in charge of an unforgiving production line with around 70 people peeling and cutting vegetables. One day they encountered a carrot which was absolutely huge. It weighed over seven pounds!&nbsp;</font></p>

            <p>
            <font face="Verdana" size="1"><b> If you could have changed one particular business decision in your life, what would this have been?&nbsp;</b><br>
 I should have become a pilot. I chose not to pursue flying as a career because I did not think it was demanding or stimulating enough but I now know that they can do other things as well because they get quite a lot of free time.  If I wasn't involved in airport management today I like to think that I may have landed up organising and managing major events such as music concerts.&nbsp;</font></p>

