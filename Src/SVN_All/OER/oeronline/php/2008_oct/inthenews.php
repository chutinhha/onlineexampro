<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
</font><font size="2" color="#CC3300" face="Verdana"><b>Oman Air takes steps for 
business continuity<br>
<br>
</b></font><font face="Verdana" size="1">
<img border="0" src="news1.jpg" align="left" hspace="6" vspace="4" width="100" height="99">Oman Air, the national carrier of Oman, 
which is growing by leaps and bounds today stands tall with a list of 
achievements to its credit. In the latest measure to ensure flawless continuity 
of services, Oman Air is setting up a data recovery centre. This will provide 
the airline with a leading edge business solution allowing all IT systems to be 
duplicated and backed up in real time and stored safely for 
<img border="0" src="news2.jpg" align="right" hspace="6" vspace="4" width="300" height="188">immediate use.<br>
<br>
Oman Air is jointly owned by the government of Oman holding a share of 82.4 per 
cent and the rest 17.6 per cent by private shareholders. Oman Air appointed Gulf 
Business Machines (GBM), one of the largest IT solutions and service providers 
in the United Arab Emirates, also IBM’s representative in the region – to 
successfully implement this state-of-the-art IT business continuity solution. 
The GBM solution includes the implementation of CISCO Systems networking 
components, involving replacing and laying fiber and UTP cabling as well as 
implementing Veritas-based replication for critical servers. The project began 
in late 2007 and will be fully operational in the coming weeks.<br>
<br>
“As the Airline business grows ever more dependent on IT services, the need for 
an alternate data centre with a proper data disaster recovery solution to ensure 
continuous availability of systems has become very crucial,” said Oman Air’s 
Head of Information &amp; Computing Habib Bhacker Habib.<br>
<br>
Says GBM General Manager Olivier Mouries, “Oman Air has an excellent IT 
infrastructure, nonetheless they needed a data recovery centre in place to 
address their growing business needs.”<br>
<br>
Habib explains that the airline partnered with GBM based on their detailed 
proposal that fully met the airline’s business requirements. GBM is also highly 
experienced in this field and has carried out many large-scale similar projects 
in the region. “We wanted to put in place uninterrupted IT services to better 
meet the needs of our customers, and GBM was chosen as an ideal partner because 
of the quality of service and value-add we received from them,” he added.<br>
<br>
Maqbool Ramadan Al Maimani, Information Technology Manager in Oman Air and 
Project Manager says, “Keeping in mind the current and future expansion 
requirements of Oman Air, the focus has been on implementing the best and most 
cost effective Disaster Recovery Solution.”<br>
<br>
Oman Air believes that business continuity is essential, and when the unexpected 
happens, it wants to be ready and resilient. In this IT-intensive world reliable 
data backup and recovery is crucial, and critical applications need to be 
available no matter what. Protection against any kind of downtime is necessary, 
and if vital data and applications go unprotected when an unexpected outage 
occurs whether it is caused by a minor software glitch, a virus outbreak or 
natural disaster at stake are customer relationships, productivity and possibly 
huge sums.<br>
<br>
Omani aviation sector has indeed undergone significant changes over the last few 
years at both the airport and airline level, and being the service provider in 
Muscat International Airport and Salalah Airport, Oman Air continuously monitors 
operations and seeks to upgrade facilities to ensure that the airport is 
equipped with the most up-to-date means of technology, to reduce operating 
costs, ensure greater flexibility and control, in addition to meeting the 
expectation of the customers. </font><p align="right" style="margin: 3px 10px"><font face="Verdana" size="1">
