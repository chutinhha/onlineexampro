                <br>
                </font><b><font face="Verdana" size="4">Turned On<br>
                </font></b><font face="Verdana" size="1"> <b>Oman’s insurance 
                industry is looking at brighter times ahead having put the ‘gonu-ised’ 
                days of the past behind them. Visvas Paul D Karra does a status 
                check of the sectors</b><br>
                <br>
                The history of Oman’s insurance sector can be classified as pre 
                and post-6th June 2007. The importance of this vital financial 
                service was highlighted when Cyclone Gonu caused extensive economic 
                losses with companies and citizens scrambling to insurance companies 
                to try and recover losses. A report by Standard &amp; Poor’s Rating 
                Services estimate’s that the total economic loss in 2007 amounted 
                to anywhere between RO3bn and RO4bn, of which RO245mn was insured.<br>
                <br>
                <strong>Turning point</strong> <br>
                For insurance companies themselves, the unusual weather conditions 
                of June 2007 worked as a double-edged sword. On the one hand, 
                they had to pay millions of rials for the cyclone claims but this 
                seminal event also drove home the growth in the sector. The underwriting 
                business it seems could not have asked for a better script.<br>
                <br>
                “Without a doubt Cyclone Gonu has been a testing period for the 
                insurance sector in Oman but it raised the awareness about the 
                need to have an insurance,” says Dr J Retnakumar, Resident Manager, 
                Oman operations, The New India Assurance Company, one of the oldest 
                foreign insurance company operating in Oman. Concurring with this 
                is A R Srinivasan, General Manager, Al Ahlia Insurance, who points 
                out that the cyclone was a real eye opener and brought to light 
                the need for insurance, especially in the urban areas. “Earlier, 
                if a person purchased a house, he or she only insured the building, 
                but after the floods destroyed household property, many people 
                are now insuring household goods including electronic items like 
                TVs, refrigerators etc,” says Srinivasan.<br>
                <br>
                According to Sanjeev Jha, Managing Director, Royal &amp; Sun Alliance 
                Insurance Oman, while there was an impact on the bottomlines of 
                the companies after Cyclone Gonu it was well contained. “At RSA, 
                we always try to balance our growth aspirations with sensible 
                underwriting. If the pricing is not adequate or the risk is not 
                something we do not have expertise in, then for us it is not worth 
                underwriting this risk. In effect, we bite what we can chew and 
                thereby ensure best in class claims service for our insured. Further 
                as insurers we always insure ourselves. There are different reinsuran 
                ce products. One of them is ‘excess of loss’ which came into play 
                during unusual events like the 2007 cyclone,” says Jha.</font></p>
              <p align="center" style="margin-left: 10px; margin-right: 10px">
				<a target="_blank" href="insurance.jpg"><img src="insurancesm.jpg" width="400" height="100" border="0"></a></p>
              <p align="justify" style="margin-left: 10px; margin-right: 10px"><font face="Verdana" size="1"> 
                <img src="insurance2.jpg" width="200" height="176" hspace="8" align="left">The 
                country has 11 national and nine foreign insurers, as well as 
                some 16 authorised insurance brokers. Despite the losses, the 
                sector bounced back with a healthy 15 per cent year-on-year growth 
                in terms of market premiums. Insurance companies in the Sultanate 
                issued 769,259 policies in 2007 compared to 648,602 in 2006 representing 
                an increase of 18.6 per cent. The total assets of national insurance 
                companies rose by 38 per cent in 2007 to RO278mn with investments 
                at more than RO235mn.<br>
                <br>
                <strong>Steady progress</strong><br>
                According to the ‘Insurance Market Review’ report for 2006-2007 
                released by the Capital Market Authority, the Sultanate’s total 
                non-life insurance premiums increased to RO137mn in 2007, up 15.9 
                per cent from RO118.2mn in 2006. The life premiums added a further 
                RO31.7mn income in 2007, up 21.6 per cent on the RO26mn the previous 
                year. <br>
                <br>
                <img src="insurance3.jpg" width="200" height="165" hspace="8" align="right">A 
                buoyant economy, a burgeoning population and a supportive regulatory 
                regime are factors that have helped the domestic retail and commercial 
                insurance sector. New comers like Oman Insurance Company have 
                been aided by such comfort factors and have seen unexpected growth 
                in their operations. “Oman Insurance Company has been well accepted 
                in the market and we hope to achieve our targets for 2008. We 
                have already generated premiums of around RO1.5mn so far,” informs 
                its country manager Anil C Kapadiya. “The confidence of insurance 
                companies is also boosted by the government policies like for 
                example, the recent move by the authorities to invest in specific 
                studies like mapping the flow of flood waters. This will help 
                insurers to make better decisions while underwriting thus strengthening 
                their books,” says Kapadiya.</font></p>
              <p align="justify" style="margin-left: 10px; margin-right: 10px"><font face="Verdana" size="1"> 
                <strong><img src="insurance4.jpg" width="200" height="166" hspace="8" align="left">Growth 
                generators</strong><br>
                Medical insurance holds a lot of potential for the insurance players 
                in Oman with the government increasingly offloading its burden 
                of taking care of the health of citizens. Many corporates are 
                now taking up group health insurance schemes for its employees 
                to get treatment from private health care providers. This has 
                encouraged Al Ahlia, National Life and New India Assurance to 
                re-launch their health insurance schemes recently.<br>
                <br>
                Insurance firms have incorporated several features like inpatient 
                and outpatient treatment both within and outside the country to 
                meet specific customer requirements. The growth of the corporate 
                sector is leading to a demand for group health insurance schemes. 
                Insurers have also been deriving strength from the government’s 
                supportive and pragmatic stance. The CMA, the regulator of the 
                insurance industry is keen to maintain a vibrant insurance sector, 
                not only to protect the country’s population and assets, but also 
                to help develop its financial infrastructure.<br>
                <br>
                <strong>Market penetration</strong><br>
                <img src="insurance5.jpg" width="200" height="163" hspace="8" align="right">Motor 
                insurance continues to occupy more than 40 per cent of the general 
                insurance companies’ portfolio while other major products include 
                policy packages offered to suit individual industry requirements 
                like fire/ industrial risks for engineering projects; marine for 
                imports and exports; goods in transit; workmen’s’ compensation; 
                public liability; medical and life insurance; fidelity and money-in-transit 
                insurance; plant and equipment and professional indemnity insurances.<br>
                <br>
                Apart from this, traditionally, the region has low insurance penetration, 
                as low as two per cent, when compared to other developing markets. 
                However this has changed since the time of Cyclone Gonu but much 
                more needs to be done in this direction. The CMA and other companies 
                are making an effort to generate awareness about insurance in 
                the Sultanate. <br>
                <br>
                <img src="insurance7.jpg" width="200" height="167" hspace="8" align="left">New 
                India Assurance recently launched the ‘Safety First, Insurance 
                Next’, an awareness campaign on environment, industrial, cyber, 
                aviation, household, road, health and child safety. Says Andrew 
                M Woodward, General Manager, Muscat Insurance, “Insurance companies 
                need to demonstrate outstanding customer service to convince a 
                largely sceptical public on the value of insurance. We have to 
                face the reality that a large sector of the population does not 
                believe in insurance, as witnessed by the fact that many new car 
                buyers do not buy comprehensive insurance. Its up to us to change 
                this situation.”<br>
                <br>
                <strong>Balanced regulations</strong><br>
                The CMA has been working as an enabler for the industry. Apart 
                from paving the way for more companies to be established in Oman, 
                the CMA’s policy of avoiding restrictive regulations, has allowed 
                the market to grow.<br>
                <br>
                Several new regulations have been introduced by the CMA which 
                has been well received by both consumers as well as insurance 
                companies. “In terms of insurance pricing, it is much more competitive 
                than other countries in the GCC. Its regulations are also quite 
                advanced.” says Deepak Kamath, Area Manager, Axa Insurance.<br>
                <br>
                <img src="insurance9.jpg" width="200" height="181" hspace="8" align="right"> 
                The Unified Motor Insurance policy focuses on transparency among 
                the parties and it would upgrade the level of coverage and insurance 
                benefits provided by insurance companies. The introduction of 
                Minor Road Traffic Accidents system has contributed to smoother 
                traffic by reducing traffic jams caused by minor road accidents 
                not involving serious injuries.<br>
                <br>
                The Orange Card system implemented recently is also expected to 
                ease the procedures for Omani vehicles entering other Arab countries 
                at border checkpoints. It will facilitate the settlement of claims 
                and indemnities related to traffic accidents outside Oman and 
                for non-Omani vehicles involved in accidents in the Sultanate.<br>
                <br>
                <strong>Challenges ahead</strong><br>
                Most insurance companies are feeling the heat of the competition. 
                Some players feel that twenty odd companies are a bit too many 
                for a market like Oman. There are at least two more companies 
                which are expected to join the fray soon. In a growing economy 
                competition cannot be wished away but in a growing market there 
                is space for everyone.<br>
                <br>
                “One of the main challenge is the uncertainty of the dollar vis-&agrave;-vis 
                regional currencies. Inflation is the second major problem which 
                has kept the premium prices low. And the third is the availability 
                of manpower, because of the boom in other countries like India,” 
                says Kamath. With the talent crunch from the Indian sub-continent 
                looming large, companies like Royal Sun Alliance are scouting 
                for talent in other countries like South Africa etc.<br>
                <br>
                Most insurers agree that maintaining competency levels is a major 
                challenge and there is no shortcut to it except training Omanis. 
                With the increase in number of players, everyone is silently mouthing 
                the word ‘enough’. But in a liberalised and competitive environment, 
                the only respite is a rationalised pricing mechanism which the 
                regulators would need to work out. </font></p>
