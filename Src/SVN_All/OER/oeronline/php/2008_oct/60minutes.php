                </font><b><font face="Verdana" size="4"> Future Ready<br>
			</font></b><font face="Verdana" size="1"><b>Thomas Frey, Google’s 
			Top-rated futurist thinker, speaks to OER about a range of issues 
			from predicting the shape of things to come to the impact of 
			inflation on the world. Frey is a keynote speaker at the upcoming 
			Leaders in Dubai conference to be held on 16-18 November 2008<br>
                </b>
                <br>
                </font><font size="1" face="Verdana, Arial, Helvetica, sans-serif"><img src="event.jpg" width="250" height="251" hspace="8" vspace="6" align="left">Thomas 
                Frey is the Executive Director and Founder of the DaVinci Institute, 
                an organization launched in 1997 as a non-profit futurist think 
                tank that has emerged as a center of visionary thought inventors 
                and business leaders in the United States. Frey is considered 
                as one of the most creative futurists on the planet. The Denver 
                Post and Seattle Post Intelligencer have referred to him as the 
                &#8220;Dean of Futurists&#8221;. <br>
                <br>
                He is also one of the leading experts on the process of launching 
                new businesses and new technologies. His talks on futurist topics 
                have captivated people ranging from high-level government officials 
                to executives in Fortune 100 companies including NASA, IBM, AT&amp;T, 
                Hewlett-Packard, Lucent Technologies, First Data, Boeing, Ford 
                Motor Company, and many more. Frey is part of a task force brought 
                together to develop Brain Fingerprinting, a patented new EEG-based 
                testing system that can determine whether specific information 
                is stored in a person&#8217;s memory. Brain Fingerprinting testing 
                measures responses to relevant words, pictures or sounds presented 
                by a computer. It has provided highly accurate results in research 
                conducted over the past 15 years. Excerpts from an exclusive interview. 
                <br>
                <br>
                <strong>Can we really predict the future?</strong> <br>
                The greatest value in understanding the future comes from 
			spotting the major </font> 
			<font size="1" face="Verdana, Arial, Helvetica, sans-serif">
			cultural, demographic, societal, and economic shifts early and 
			translating them into viable business opportunities. Our ability to 
			predict the future can only be gauged in probabilities. Some tools 
			and techniques give us a higher probability of forecasting the 
			future, but there are no absolutes – and there shouldn’t be. There 
			is an obvious downside to knowing the future. Too much certainty 
			about the future would cause us to stop taking risks and essentially 
			suck the life out of 
                our planet. The fact that the future is unknowable is our greatest 
                motivator. I think that it is important to think about the future, 
                because that is where our children live. We have nobody that is 
                looking out for the best interests of our children, grandchildren, 
                and great grandchildren. We as human beings tend to be short-term 
                thinkers, and we are incurring great amounts of debt &#8211; I 
                think that&#8217;s doing a great disservice to future generations.<br>
                </font><br>
                <font size="1" face="Verdana, Arial, Helvetica, sans-serif"><strong>What 
                are the issues that we should raise now in order to have a better 
                future?</strong> <br>
                While we cannot predict the future with certainty, we can manage 
                it. We can manage it by creating incentives for specific outcomes, 
                as was done with the X-Prize, DARPA Grand Challenge, and many 
                more. <br>
                <br>
                Most major problems the world faces today can be solved with a 
                challenge attached to an incentivised outcome. Rewards for accomplishing 
                the challenges need to be commensurate with the accomplishment 
                (i.e. Curing cancer should have a substantial prize associated 
                with it.) Such incentive prizes have a way of focusing attention, 
                energy, resources and money on a problem. <br>
                <br>
                <strong>Religion vs. science: which is more successful in predicting 
                the future? </strong><br>
                Both are poor predictors. Science can predict with high probability 
                the movement of planets for years to come, or the melting pattern 
                of ice, or the result of a laser blast. Religion, on the other 
                hand, has a way of &#8220;influencing outcomes&#8221; with higher 
                degrees of probability; but as a society, the things we value 
                most, the relationships and conversations between people, the 
                value judgments we make, and the things we hold in highest regard, 
                are all still too complicated to predict &#8211; and they should 
                be, as I mentioned, because complacency suffocates motivation. 
                <br>
                <br>
                <strong>The best way to predict the future is by creating it, 
                so how can investors/employees foresee their company&#8217;s future? 
                And, how can companies foresee their customers&#8217; needs?</strong> 
                Company futures and customer needs are a moving target. Nothing 
                is static. Customer needs are also a moving target. Business is 
                all about taking risks, and that is what makes it exciting. Formulaic 
                business methodologies that work &#8220;too well&#8221; tend to 
                breed complacency and forestall innovation. Managing against complacency 
                becomes the higher risk failure point in business rather than 
                markets and sales, whereas innovation usually produces market 
                leaders. Rather than accurately predicting the future, companies 
                should focus on improving probabilities for success. Even a one 
                per cent improvement in the probability for success will give 
                companies an unusual competitive advantage.<br>
                <strong><br>
                What is the impact of &#8216;bad&#8217; knowledge on a business?</strong><br>
                &#8220;Bad knowledge&#8221; is an illusion when it comes to business 
                decisions. We only know if business knowledge is good or bad after 
                the fact. Studies show that 15 per cent of what the average person 
                thinks they know is &#8220;confidently held misinformation&#8221;.</font><font size="1" face="Verdana, Arial, Helvetica, sans-serif"><br>
                </font><br>
                <font size="1" face="Verdana, Arial, Helvetica, sans-serif"><strong>The 
                world is rapidly changing, and globalisation is changing, and 
                in some instances, erasing world cultures? How can reserve our 
                culture in the future?</strong><br>
                Most people think of culture as the clothes people wear, homes, 
                buildings, dance, art, rituals and other visible symbols of a 
                community of people. However, it is much more, and at the same 
                time, much less. <br>
                <br>
                </font> 
			<font size="1" face="Verdana, Arial, Helvetica, sans-serif">Culture results from the shared experiences of a community of 
                people and their system for preserving these experiences from 
                one generation to the next &#8211; but culture has always been 
                evolving, and our new web-based systems are shifting the idea 
                of what a community is. Where communities in the past were 
                based on geographic proximity, today we have communities based 
                on areas of interest (mountain climbing or archeology), work related 
                communities (co-workers), research- related communities (people 
                studying nutrition or genealogy), religious-based communities, 
                and much more. Each of these areas has a potential for developing 
                its own kind of culture.<br>
                <br>
                Moreover, contributing to the changing culture is our mobile society, 
                where people tend to shift from participants to observers. As 
                the participants dwindle, pieces of culture will evaporate. Yes, 
                globalisation is changing culture, but so is our technology, our 
                communication systems, our religions and our mobility. This is 
                neither right nor wrong, good nor bad. Culture will continue to 
                evolve, and so will our languages. <br>
                <br>
                <strong>How will the current state of global inflation shape the 
                world economy? What do you see coming? </strong><br>
                Extreme inflation, as is happening in a few places around the 
                world, is the result of people losing confidence in their national 
                currency (fiat money &#8211; based on trust). This often happens 
                when a governmental leader decides to print far more money than 
                their economy can absorb, or engages in some other unscrupulous 
                activity, but sometimes results from radical swings in the global 
                economy. <br>
                <br>
                Inflation, however, is never uniform. It typically hurts the 
			developing nations more because they do not have the sophisticated 
			checks and balance systems for control. They end up reacting much 
			more slowly and losing ground in the global marketplace just from 
			the delays. Global inflation has a way of expanding the blighted 
			areas, causing systems to stop working, and economies to shift into 
			survival mode. I would suggest that using a global currency is an 
			interesting approach to balancing out the highs and lows of national 
			currencies. <br>
                <br>
