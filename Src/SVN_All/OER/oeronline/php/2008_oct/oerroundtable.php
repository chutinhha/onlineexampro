            <td width="70%" bgcolor="#FFFFFF" valign="top"> <p align="justify" dir="ltr" style="margin-left: 5; margin-right: 5"><b><br>
                </b><b><font face="Verdana" size="4">Is marketing coming of age 
                in Oman?</font></b><font face="Verdana" size="1"><br>
                <b>Marketing heads of companies share their thoughts on the challenges 
                and opportunities that Oman’s market presents</b></font></p>
              <table width="90%" border="0" align="CENTER" height="570">
                <tr>
                  <td width="64%">
