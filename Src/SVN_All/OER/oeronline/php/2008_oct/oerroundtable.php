              <p align="justify" dir="ltr" style="margin-left: 5; margin-right: 5"><font face="Verdana" size="1"> 
                <font color="#990000"><b> </b></font><strong><font face="Verdana" size="1"><strong><img src="oer1.jpg" width="150" height="316" hspace="8" vspace="6" align="right"></strong></font><font color="#990000">OER:</font></strong> 
                <font color="#990000"><strong>Buy one get one free has traditionally 
                been the way marketing was done in Oman. Have things changed and 
                have they changed enough?</strong></font><br>
                <br>
                <strong>MICHAEL</strong>: Oman has been a promotion driven market 
                but things are changing. For example at Nawras we are trying to 
                change that approach. We look at creating value for the customer 
                rather than coming up with a one-size fits all approach. With 
                new players entering the market we hope things will change further.<br>
                <br>
                <strong>ABEER</strong>: For us things are different, we cannot 
                give a house for free but we are definitely looking at making 
                things more convenient for the customer like making it easier 
                for people to get in touch with us.<br>
                <br>
                <strong>TALAL</strong>: The whole concept of marketing is changing. 
                While a big segment of the market still looks for promotions, 
                people are gradually realising the importance of brands and a 
                certain measure of brand building is coming in.<br>
                <br>
                <strong>PANKAJ</strong>: We have been trying to make offers to 
                the customers which add value to the product like free insurance, 
                registration etc. At the same time it is very important for a 
                brand to have a bigger mindshare thus promotions and brand building 
                needs to happen at the same time. We have started doing a lot 
                of brand building in the last two-three years. Different brands 
                have different positioning in the customers’ minds and they sell 
                in a different manner altogether. You will not see the same thing 
                happening on a BMW, Nissan or Infiniti.<br>
                <br>
                <strong>MAZIN</strong>: Lets take the case of our bank. It was 
                a new bank that opened after 15 years since the last bank entered 
                the market. So we had to think about establishing our brand. My 
                competitor has been here for 27 years and we need to compete with 
                them. Our strategy is to differentiate. So when Bank Sohar enters 
                a segment we need to have products that are different. More than 
                the name of the bank it is important for customers to know as 
                to how the bank is going to add value.<br>
                <br>
                <strong>MOHAMMED</strong>: We heard the comments that it is more 
                about promoting the brand than about promotions. For BankMuscat 
                (BM), the strategy is to retain the leadership of the brand. We 
                are also working on a strategy to expand regionally and become 
                a leading player outside Oman. As BM expands the challenge for 
                us is to have campaigns that will sustain the brand. <br>
                <br>
                <strong><font face="Verdana" size="1"><strong><img src="oer2.jpg" width="150" height="320" hspace="8" vspace="6" align="left"></strong></font><font color="#990000">OER: 
                Is marketing only about selling a product or also about sustaining 
                a customer experience and are marketers in Oman working towards 
                it?</font></strong><br>
                <strong><br>
                PANKAJ</strong>: We focus on both. In the last two years we have 
                doubled our service and sales network to give customers a total 
                ownership experience of both sales and service which goes into 
                building the retail brand identity. The retail environment gives 
                the customer a feeling which is consistent with the global feel.<br>
                <br>
                <strong>TALAL</strong>: When we talk of marketing and retaining 
                customers, we have to distinguish between two things; one what 
                kind of promises we make to the customer and whether we can deliver 
                on them. As customers get more educated and informed, delivering 
                on promises is becoming more important as any adverse reaction 
                can damage a product or brand substantially. <br>
                <strong><br>
                MICHAEL</strong>: I think that Talal has made a good point on 
                how to manage expectations. At Nawras we continuously track customer 
                experience through surveys, studies, mystery shopping exercises 
                etc. Each of our customer touch points is constantly measured. 
                Instead of waiting for customers to comment on some aspect of 
                our service we proactively ask for their feedback. Customer expectations 
                are increasing almost daily across the board due to greater exposure 
                and one cannot afford to take it lightly. Our chief goal in 2009 
                is to be sharper and nimble.<br>
                <br>
                <strong>MOHAMMED</strong>: A number of companies do not have a 
                feedback mechanism. All companies should have a feedback system 
                both internal and external.<br>
                <strong><br>
                ABEER</strong>: A brand like The Wave can be everywhere through 
                advertisements, but if we are not delivering in terms of construction, 
                quality and timelines than irrespective of all the marketing activities 
                the brand is going to suffer. We have to manage people’s expectations 
                by advertising and by delivering.<br>
                <br>
                <strong>MAZIN</strong>: Since we needed to catch up with the market 
                initially for a month we had campaigns only talking about the 
                bank. Then we went in for one umbrella brand for our products 
                called Al Mumayaz. We wanted people to immediately think of Bank 
                Sohar whenever they heard the word Al Mumayaz. Marketing needs 
                to be backed up by good service.<br>
                <br>
                <strong><font face="Verdana" size="1"><strong><img src="oer3.jpg" width="150" height="317" hspace="8" vspace="6" align="right"></strong></font><font color="#990000">OER: 
                There is lack of television audiences, you don’t have radio, and 
                the print media has a limited reach. Is the lack of media options 
                in Oman a challenge?</font></strong><br>
                <br>
                <strong>MAZIN</strong>: This is definitely a challenge in Oman, 
                print is the only medium for advertisers and it has its limitations. 
                The other challenge is to find a good advertising agency and this 
                is leading a number of companies to look beyond Oman to get their 
                creative work done. Most agencies in Oman have a large number 
                of expatriates, but 90 percent of my target audience is Omani. 
                As a result most advertisements are prepared in English and they 
                may be very powerful but when they get translated into Arabic 
                they lose their punch. The silver lining is that people earlier 
                thought of marketing as a cost but today it is seen as an investment.<br>
                <br>
                <strong>TALAL</strong>: In Oman one needs to be innovative to 
                counter the limited reach of the media. For example at ITA we 
                decided to do road shows to popularise e-services. We went to 
                different regions, spoke to people one on one and got our message 
                across. On the positive side Oman is a relatively cheaper market 
                compared to other countries in the region. And it is easier to 
                market in Oman compared to a Dubai or Saudi Arabia.<br>
                <br>
                <strong>PANKAJ</strong>: I do not agree that Oman is a relatively 
                easier market for a number of reasons – the country is geographically 
                spread out, the customers have a number of choices. Most of our 
                customers reside in the interiors and there is no media which 
                has a mass appeal. Television and radio are in a very nascent 
                state. These are the various challenges as far as media is concerned. 
                The existing media is becoming more fragmented as almost everyday 
                more weeklies, dailies and magazines are coming out, we don’t 
                know where and in which ones to advertise. Taking a decision on 
                the media itself is quite a gamble and most often we go by word 
                of mouth. <br>
                <br>
                One medium we depend on is gathering database and sending bulk 
                SMS because we know everyone has a mobile. Advertising agencies 
                have their own limitations as they juggle between different accounts 
                and the quality of job that you can get done is quite questionable.<br>
                <br>
                <strong><img src="oer4.jpg" width="150" height="314" hspace="8" vspace="6" align="left">ABEER</strong>: 
                I think it is easier for us because we are a new product. Our 
                challenge is to find creative agencies since our customers are 
                exposed to international TV and the Internet. Advertising agencies 
                in Oman here cannot deliver that quality and that is a major handicap.<br>
                <br>
                <strong>MOHAMMED</strong>: I agree that local agencies are not 
                upto international standards but we need to look at the issue 
                from another perspective. If all companies go in for international 
                ad agencies then we are not giving the local agencies the right 
                tools to improve themselves making us dependent on international 
                agencies. International agencies have their own limitations like 
                the distance, a different culture etc. Global companies are now 
                concentrating on CSR to support the community. Similarly local 
                companies should support local agencies and this will help agencies 
                to hire more skilled people. For certain strategic campaigns we 
                need to hire international agencies but for other campaigns I 
                think the local ad agencies can deliver.<br>
                <br>
                <strong>MICHAEL</strong>: Since we are a new player in this market, 
                we have to ensure that our communication is better and therefore 
                we have to work harder on our strategic thinking. And that is 
                the message we give our people – we need to be smarter and we 
                need to be less traditional. I would not compare this market to 
                some of the markets in Europe and I couldn’t compare it to any 
                of the GCC markets either because it is unique, it has its own 
                culture and systems. I would be the last person to come here and 
                try to force upon this market a different sensibility. One has 
                to adapt and improve things.<br>
                <br>
                <strong><font color="#990000">OER: Looking at marketing from a 
                futuristic point of view, have marketers started using technology 
                adequately? </font></strong><br>
                <br>
                <strong>TALAL</strong>: Marketers were not using technology earlier 
                but now they have started using it. The most popular medium being 
                direct SMS, companies have already started using e-mails to reach 
                out to customers but it is not enough. The main challenge that 
                we face is the lack of adequate infrastructure like the Internet 
                being weak outside Muscat and poor awareness.<br>
                <br>
                <strong><img src="oer5.jpg" width="150" height="313" hspace="8" vspace="6" align="right">MOHAMMED</strong>: 
                Even people within Muscat who are using the Internet find it difficult 
                to connect and its reliability is an issue. The number of hits 
                that websites get is also not encouraging. The market is still 
                not clear about the use of Internet. The major challenge is to 
                create awareness and to improve the infrastructure.<br>
                <br>
                <strong>ABEER</strong>: For us the choice of technology is driven 
                by the market, we have enough people using the Internet and SMS. 
                If we find that more people are reading newspapers and magazines 
                than we would use the print media. Once more people start using 
                the Internet then marketing will get IT driven.<br>
                <br>
                <strong>MAZIN</strong>: Personally I hate to receive an SMS at 
                odd hours but it definitely has the reach. If you can reach one 
                million people or even 50,000 with one message then it is cost 
                effective. Out of these even if five percent of the people read 
                the message then you have done it.<br>
                <br>
                <strong>PANKAJ</strong>: When you talk about technologies like 
                the SMS and the Internet I believe that it makes the job of marketers 
                more exciting. The use of technology not only helps one to do 
                things differently but it also opens up a number of possibilities 
                like sponsorships and direct marketing. <br>
                <br>
                <strong>MICHAEL</strong>: Technology helps us to highly personalised 
                e-mails, put up messages on websites. We have the benefit of directly 
                communicating to our customers but we are careful that all messages 
                are related to our services. <br>
                <br>
                <strong><font color="#990000">OER: Has marketing become more sophisticated 
                in the last few years? </font></strong><br>
                <br>
                <strong>MAZIN</strong>: It is a new market and we can see that 
                people are being influenced by marketing efforts of companies. 
                Our feedback confirms this trend. People are evaluating companies 
                based on their marketing and services. The customer is becoming 
                smarter and marketers have to be straightforward with them. Companies 
                are using new techniques to reach out to the customer and so if 
                we compare ourselves to where we were five years ago then things 
                have changed.<br>
                <br>
                <strong><img src="oer6.jpg" width="150" height="321" hspace="8" vspace="6" align="left">ABEER</strong>: 
                As the customer becomes more sophisticated and aware the market 
                usually reflects that. If marketers do not keep pace with the 
                changes then we will not be able to sell. In any country marketing 
                usually reflects the mindset of a society.<br>
                <br>
                <strong>TALAL</strong>: I agree with Abeer that marketing mirrors 
                mindsets. The emergence of sunrise sectors is helping the evolution 
                of marketing in Oman. For example the quality of advertisements 
                has improved and so has the quality of publications. <br>
                <strong><br>
                PANKAJ</strong>: In the past two to three years there is buoyancy 
                in the market. There are new sectors like telecom, real estate, 
                retail which have come up. Over a period of time as the market 
                opens up the marketshare of established players will come down. 
                There are different segments of customers with their own set of 
                priorities. For example an Armada customer would want his car 
                to be explained inside out whereas a Tiida customer would just 
                come in to negotiate the price. Overall the market is getting 
                more sophisticated and marketers need to work more intelligently. 
                <br>
                <strong><br>
                MICHAEL</strong>: There is a potential for the market to grow 
                but as competition intensifies there is going to be a challenge 
                for incumbent’s to hold onto their market share both in terms 
                of numbers of customer and share of value. We are placing ourselves 
                in a strong position to ensure that we protect both these elements 
                of our market share. Undoubtedly, the way we communicate is going 
                to become sophisticated but the key for us is not to lose sight 
                of the customer. So even though there is going to be complexity 
                in the way we communicate, the message has to be simpler. It is 
                all about keeping the message relevant so that the customer sees 
                value in choosing us compared to our competitors. <br>
                <br>
                <strong>MOHAMMED</strong>: Let’s track some of the marketing trends. 
                A lot of the companies who never advertised have started advertising. 
                Even NGOs have started advertising. Another trend is the introduction 
                of many publications. The third trend is the growing demand for 
                talented marketing professionals. This shows the growing importance 
                of marketing.</font></p>
