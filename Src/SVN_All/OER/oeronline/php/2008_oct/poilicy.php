                </b><font face="Verdana" size="1"> <font face="Verdana, Arial, Helvetica, sans-serif"><img src="policy2.jpg" width="300" height="78"></font></font></p>
              <p align="justify" dir="ltr" style="margin-left: 5; margin-right: 5"><font size="1" face="Verdana, Arial, Helvetica, sans-serif"><strong>The 
                tardy pace of the implementation of the Oman-US FTA is holding 
                back the full actualisation of two way trade. Mayank Singh reports</strong></font></p>
              <p align="justify" dir="ltr" style="margin-left: 5; margin-right: 5"><font size="1" face="Verdana, Arial, Helvetica, sans-serif"><img src="policy.jpg" width="189" height="248" hspace="8" align="left">Oman 
                and the US signed a Free Trade Agreement (FTA) on January 19, 
                2006. A Royal Decree was issued on 26 November, 2006 ratifying 
                the FTA, but the treaty is still to be implemented. “It usually 
                takes two years to implement a FTA but we have taken a bit longer,” 
                says Dr Said Amer Al Riyami, Economic Expert, DG of organisations 
                and commercial relations, Ministry of Commerce and Industry. <br>
                <br>
                Trade between the two countries has gone up in anticipation of 
                the FTA. In 2007 US exports to Oman grew by 27.8 per cent to reach 
                $1.1bn. Oman’s exports to the US rose by 14.6 per cent over the 
                corresponding period in 2006 to touch $1bn in 2007. Says Nisreen 
                Ahmed Jaffer, Director General of Investment Promotion, OCIPED, 
                “We feel that more products will find a market in the US once 
                the FTA comes into force as Omani products can enter the US without 
                any import duty.”<br>
                <br>
                Industry expects to benefit in a number of ways from the FTA. 
                For example the FTA is expected to revive the dying textile industry 
                in the Sultanate by exempting textile products from Oman of the 
                prevailing 12 per cent import duty in the US. Secondly, upto 50mn 
                sq mtrs of textiles would be allowed to enter the US market without 
                the rules of origin clause for ten years. The rule of origin law 
                requires at least 35 per cent of the value addition to be done 
                in Oman (or any country) to a product for it to be eligible for 
                duty exemptions in the US. The FTA will also lead to more foreign 
                direct investment in Oman. “We expect other countries to invest 
                in Omani joint ventures so that they can export to the US in a 
                duty free manner,” says Riyami.<br>
                <br>
                <strong>Inexcusable delay</strong><br>
                Given such benefits, every single day that is lost in the implementation 
                of the FTA is a major loss for companies in Oman. So what is causing 
                this inordinate delay in implementing the FTA? (The office of 
                HE the Minister of Commerce and Industry refused to answer OER’s 
                questions about the FTA while the US embassy declined an interview 
                request).<br>
                <br>
                <img src="policy1.jpg" width="150" height="176" hspace="8" align="right">OER 
                has learnt from ministry sources (who did not want to be quoted) 
                that the two major areas holding up the implementation of the 
                FTA are – the liberalisation of the telecom sector and policy 
                changes changes in the Sultanate’s software policy. The Sultanate 
                had committed to liberalise the services sector when it signed 
                the World Trade Organisation (WTO) agreement in November 2000. 
                It has been eight years since and the goal still remains elusive. 
                For example the fee for a fixed line license in Oman stands at 
                $50mn. This is seen as being restrictive to the entry of foreign 
                players and is an issue that needs to be resolved.<br>
                <br>
                A WTO Trade Policy Review document on Oman issued in June 2008 
                states, ‘Services constitute a crucial component in Oman’s overall 
                policy of economic diversification. Private sector participation 
                is being encouraged by removing obstacles to foreign investment. 
                Several SOEs in the sector operate under monopoly, or hold exclusive 
                rights in some branches (e.g. Oman Telecommunications Company, 
                Oman Postal Company and Oman Air). Under the General Agreement 
                on Trade in Services, Oman made commitments in all services categories, 
                and has tabled its initial conditional offer in the ongoing services 
                negotiations.’ <br>
                <br>
                Riyami springs to the defense of the government, “We have covered 
                a long distance, almost 90 per cent of the policy revisions have 
                been done and very little is left to be done.” But till the remaining 
                space gets covered companies in Oman and abroad will have to continue 
                their anxious wait.</font></p>

