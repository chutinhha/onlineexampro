                <font face="Verdana" size="1"><br>
                Akbar Habib, CEO, ONIC Holding caused a ripple across corporate 
                Oman by putting in his papers on September 16, 2008. Habib served 
                as Chairman of the Board of Oman Investment &amp; Finance Co; 
                Chairman of the Board of Onic Holding; Deputy Chairman and a Director 
                of Al Ahlia Insurance Company, over his tenure. When Habib joined 
                ONIC in 2003 the company was going through a rough patch. Its 
                subsidiaries like National Life were making losses while Al Ahlia 
                was a mediocre company.
                ONIC’s underlying assets like Oman ORIX Leasing Co and Gulf Warranties 
                were also going through a tough time. Habib played a seminal role 
                in restructuring of a number of companies within the ONIC fold. 
                Speculation is rife about his next move, but given his credentials 
                one can be sure that Habib will be getting offers from a number 
                of companies in Oman and within the region. The possibility of 
                him doing something on his own also cannot be ruled out. Keep 
                watching this space for his next move..</font></p>
