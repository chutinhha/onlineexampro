              <p align="justify" dir="ltr" style="margin-left: 5; margin-right: 5"><b> 
                <font face="Verdana" size="4">‘Talks’ of the town</font></b><font face="Verdana" size="1"><br>
                <strong>Karim Boukarroum promises to redefine communications solutions 
                with his new venture<br>
                </strong><br>
                <b><font face="Verdana" size="1"><img src="newsmakers1.jpg" width="150" height="206" hspace="8" vspace="6" align="right"></font></b> 
                Guess who has been bitten by the entrepreneurial bug. Karim Boukarroum, 
                former head of marketing, Ahil Bank, Oman has ventured into the 
                communications field with the launch of Talks an integrated communication 
                solutions in advertising, public relations, corporate branding, 
                events and sports management, as well as online branding and marketing 
                consultancy. <br>
				<br>
				Boukarroum is the CEO of Talks. Commenting on the 
                launch Boukarroum says, “Oman is booming. It is developing at 
                an exponential rate due to the expansion in real-estate, tourism, 
                telecommunications, fossil fuels and other essential business 
                sectors. To sustain this growth we believe a robust communication 
                infrastructure has to be imbedded into the culture. The firm is 
                the first of its kind in the country to offer integrated communication 
                solutions.<br>
                <br>
                “Let’s talk” is the ambitious agency’s ethos and approach to business. 
                The brainchild of a group of experienced business professionals, 
                Talks’ mission is to “be heard”. This means achieving excellence 
                for its clients through strategic communications, strong marketing 
                and great customer service. <br>
                <br>
                Talks is positioned to respond to the escalating needs of the 
                region. Talks will offer its broad range of services to all areas 
                in the Sultanate before expanding into other GCC markets. Already 
                boasting an impressive client roster, Talks will continue to attract 
                more business by providing high-quality, creative, innovative 
                and client focused services. The team is made up of award winning 
                staffs who bring with them more than 50 years of combined experience 
                in marketing – both here in the Gulf and overseas.<br>
                <br>
                Says Boukarroum, “The decision to open Talks came from a lack 
                of relevant communication services in the marketplace especially 
                in Arabic. We want to work together with other communications 
                partners to fill this gap and improve the industry. We expect 
                advertisers to support this belief and start considering communications 
                as a major part of their marketing activities and we look forward 
                to working with them in the future.” </font></p>
