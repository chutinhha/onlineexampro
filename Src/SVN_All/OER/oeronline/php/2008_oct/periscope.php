                </b><b><font face="Verdana" size="4">DOLLAR AND THE GCC LIQUIDITY 
                SHOCK</font></b><font face="Verdana" size="1"><br>
                <b>Though the Gulf stock markets are facing the brunt of a strengthening 
                dollar and declining oil prices this could prove to be short term 
                dip as macro economic factors take a u-turn in the next few months</b><br>
                <font color="#990000"><b><br>
                By Matein Khalid</b></font><br>
                <br>
                <img src="peri1.jpg" width="107" height="126" hspace="8" align="left">The 
                global financial markets have taken an ominous turn. The Gulf 
                stock markets have become the latest victim of the bear runs that 
                have gutted stock market values from Shanghai to Mumbai, Moscow 
                to Wall Street. The reasons for the steep fall in the Gulf stock 
                markets have their origins in the macro equations of international 
                finance. <br>
                <br>
                <strong>Looking at the reasons </strong><br>
                One, when the dollar soared against the Euro from $1.60 to $1.38, 
                GCC central banks no longer had to buy dollars to maintain their 
                FX (foreign exchange) pegs. In essence, GCC money supply growth 
                came to a sudden halt. Since deep, liquid domestic corporate bond 
                markets do not exist in the region, the impact of the money supply 
                growth fell on the bank balance sheets. There is an extraordinarily 
                high correlation between oil prices and the dollar as well as 
                between GCC money supply and banking system growth. The immediate 
                impact of lower bank lending was felt in the regional stock markets.<br>
                <br>
                The GCC stock markets, under pressure in any case by the plunge 
                in crude oil and distress selling by foreign fund managers, succumbed 
                to the liquidity shock that convulsed the financial markets in 
                August.<br>
                <br>
                The GCC currency forward markets also suggest that foreign money 
                betting on a GCC FX revaluation and abandonment of the dollar 
                peg has abandoned any hope of success. As much as $100bn in speculative 
                capital has been repatriated from the GCC money markets as the 
                markets realised that a higher dollar against the Euro and regional 
                geopolitics made a FX revaluation inconceivable in the Gulf.<br>
                <br>
                Hot money contributed to the liquidity bubble and over heated 
                money markets in the Gulf in 2007. Hot money has now dealt the 
                GCC markets a liquidity shock. The regional central banks do not 
                have the full spectrum of money market instruments to ease monetary 
                policy as long as the Fed is on hold. The divergence between the 
                US and Gulf monetary and business cycles could not be more dramatic.<br>
                <br>
                <img src="peri.jpg" width="250" height="200" hspace="8" align="right">Trends 
                in the capital markets have also contributed to the liquidity 
                shock in the GCC. The recent ruling by the AAOIFI standards board 
                in Bahrain that most GCC sukuk issues do not meet Sharia compliance 
                criteria (physical attachment of collateral) had led to a dramatic 
                widening of credit spreads in even the most liquid sukuk.<br>
                <br>
                Moreover, the financial markets have also concluded that the GCC 
                property market boom will end in a classic debt deflation, as 
                happened in Asia (1998), Spain, Britain and the US in 2007-08 
                or in the Gulf itself a generation ago. This is the reason Omani 
                banks and Dubai property developers have lost more than 50 per 
                cent of their value in the last two months. There has also been 
                a bearish re-pricing of Gulf sovereign risk in the credit default 
                swap market. For instance, Abu Dhabi and Qatar credit default 
                spreads swap spreads trade at 50-60 basis points but Dubai trades 
                at 250 basis points in the capital markets. As credit risk was 
                re-priced higher once the Wall Street credit crunch deepened, 
                the capital markets were merciless to any borrower without substantial 
                oil reserves or conservative balance sheets without debt or exposure 
                to real estate risk.<br>
                <br>
                <strong>Banks lose steam </strong><br>
                GCC banks can no longer finance the speculative froth in the property 
                markets. It is rational to expect hundreds of developers with 
                cash flow problems and several dozen major corporate defaults 
                across the region if the cash crunch in the banking system continues 
                for a <img src="peri2.jpg" width="187" height="357" hspace="8" vspace="6" align="left">protracted 
                period, as I expect it will. GCC central banks are also worried 
                about the parabolic increase in consumer debt, even the chilling 
                risk of banks funding off-plan property speculation with unsecured 
                personal loans. Leverage is the kiss of death when credit is rationed, 
                banks scramble for funding share and property markets fall and 
                margin call results in distressed selling. This is a macro scenario 
                that should not be dismissed as impossible by even diehard bulls 
                (is there any other kind in a bull market?) in the GCC stock and 
                property markets.<br>
                <br>
                The dollar rally since July stemmed from the plunge in crude oil 
                and commodities, the softness in the Eurozone data and expectations 
                of an imminent ECB rate cut, Russia and Georgia’s brief was in 
                South Ossetia (the EU relies on Russia’s Gazprom for one third 
                of its imports). This means the Euro is now hostage to the capricious 
                geopolitics of Kremlin-Washington relations, as the Deutschemark 
                was to Soviet-US ties during the Cold War. The improvement in 
                the US trade balance, M&amp;A demand for the greenback and repatriation-hedging 
                flows from American pension funds desperate to escape from the 
                bloodbath in West European, Asian and emerging markets stock exchanges 
                all contributed to the dollar’s swift, spectacular ascent.<br>
                <br>
                Above all, the fabled carry trade in the currency market has now 
                blown up with a vengeance, as both the Australian and New Zealand 
                central banks have slashed interest rates, thus removing their 
                high yield attraction to Japanese investors.<br>
                <br>
                It is critical to forecast the dollar-euro rate to see if the 
                liquidity shock pressures on the Gulf will increase or decrease. 
                I believe the liquidity scenario will improve in the Gulf as the 
                dollar rally is living on borrowed time. Why? One, US macro data 
                is deteriorating. The payroll data will show 100,000 jobs lost 
                every month this winter. The unemployment rate will rise. Lehman 
                Brothers proves that systemic risk is a sword of Damocles on Wall 
                Street. Meanwhile, the plunge in oil and the higher dollar means 
                lower imported inflation as GDP weakens. I believe the US economy 
                entered a recession in September. This means a Fed rate cut, not 
                a rate hike, as is priced in the capital markets. A Fed rate cut, 
                coupled with a $600-700bn budget deficit (Fannie, Freddie, Detroit, 
                bank failures, Iraq, Afghanistan, sluggish tax revenues etc), 
                all means that the Euro could well rise to $1.48-$1.50. This will 
                help GCC credit spreads to compress.</font></p>
