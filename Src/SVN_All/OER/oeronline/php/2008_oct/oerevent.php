		        <td valign="top" width="100%"> <p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
                    </font><b><font face="Verdana" size="4">Showcasing Excellence</font></b><font face="Verdana" size="1"><br>
                    <b>Abdullah Al Jufaili, Director, Intilaaqah speaks to OER 
                    about the recently launched Intilaaqah Awards 2008</b></font><p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"> 
                    <br>
                    <img src="oerevent1.jpg" width="400" height="216" hspace="8" vspace="6" align="left"><strong>What is the big idea behind the awards?</strong><br>
                    Intilaaqah programme is a replication of Shell LiveWIRE, a 
                    Shell initiative that has been in existence for 27 years. 
                    Shell LiveWIRE provides training and counseling services for 
                    young entrepreneurs. Oman was the first country in Middle 
                    East to adopt the programme in 1995. Intilaqaah means ‘taking 
                    off.’ The idea is to encourage entrepreneurship by felicitating 
                    young Omani entrepreneurs. The award is an annual competition 
                    organised by Intilaaqah to recognise successful entrepreneurs.<br>
                    <br>
                    <strong>The awards have been launched, what happens next?</strong><br>
                    Entry forms will be distributed by DHL, our award associates 
                    in all Shell Select stores throughout the Sultanate. The form 
                    will also be available online at (www.intilaaqah.org). People 
                    interested in participating in the awards can pick up a form 
                    from any of these places. The form asks for some basic information 
                    like the participants name, gender, date of birth, contact 
                    information, commercial registration number, initial capital, 
                    and a brief on the company and the area in which the company 
                    is based. There is a special category of awards for Sultan 
                    Qaboos University Students in which they are judged on their 
                    business ideas. Usually we look at businesses that have been 
                    around for 18 months to three years.<br>
                    <br>
                    <strong>Take us through the judging process?</strong><br>
                    We receive hundreds of applications which go through a verification 
                    process. We make calls to people directly to verify their 
                    claims. We filter out the applications to zero in on ten participants 
                    based on the uniqueness of their business and the viability 
                    of their model. They are then handed over a more extensive 
                    form running into six to seven pages, once their responses 
                    come in we pay a visit to their sites to get more information 
                    on their business. All these inputs are given to a judging 
                    panel. The judges then select three finalists based on their 
                    performance on various parameters like the communication skills 
                    of the owner, the participants marketing and managerial skills, 
                    its health, safety, environment (HSE) record, differentiation 
                    of the business idea etc. The three finalists are given a 
                    chance to give a presentation on their business strategy, 
                    their financial roadmap in future and how they propose to 
                    use the award money. The winners will be announced at the 
                    Intilaaqah function towards the end of December 2008.<br>
                    <br>
                    <strong>What do the winners take home?</strong><br>
                    The three finalists will get a full year free subscription 
                    of A’alamal Iktisaad Wal Amal and Oman Economic Review and 
                    promotional opportunities through our media partners. They 
                    will get an opportunity to avail of a RO10,000 loan from Sharakah 
                    plus the prize money of RO5000, RO3000 and RO2000 for the 
                    three winners. The winner also qualifies for the Intilaaqah 
                    Regional award. <br>
