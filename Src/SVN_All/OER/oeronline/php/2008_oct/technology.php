		        <td valign="top" width="100%"> <p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
                    </font><b><font face="Verdana" size="4">Era of E-Payments</font></b><br>
                    <font face="Verdana, Arial, Helvetica, sans-serif" size="1"><strong>The 
                    national e-payment gateway launched by the information technology 
                    authority takes electronic funds transfer (EFT) to a new level 
                    of maturity in the Sultanate</strong><br>
                    <br>
                    </font><font face="Verdana" size="1"><img border="0" src="tech.jpg" align="left" hspace="6" vspace="4">The 
                    cycle of commerce cannot be complete without a payment and 
                    this is more critical when goods or services are delivered 
                    online. With the newly launched National ePayment Gateway, 
                    Oman is now blazing into a new era of ecommerce and e-government.<br>
                    <br>
                    <strong>Electronic Fund Transfers</strong><br>
                    Payment through electronic means has been happening since 
                    a very long time. Electronic Fund Transfer (EFT) system is 
                    a cluster of technologies enabling the execution of financial-transactions 
                    by means of electronic messages without requiring paper-based 
                    instrument of exchange. Most EFT systems involve computers, 
                    networks and telecommunication links, access equipments and 
                    automated data exchanges. <br>
                    <br>
                    Some examples of such systems are Automated Clearing Houses 
                    (ACH), Automated Teller Machines (ATM), point-of-sale terminals 
                    (POST) used at shopping malls, telephone-based bill payment 
                    systems, online banking, etc. The implementation of the Real 
                    Time Gross Settlement System (RTGS) in September 2005 and 
                    the Automated Clearing House (ACH) system in September 2006 
                    has enabled completely automated safe and speedy transfers 
                    of funds between the financial institutions of the Sultanate. 
                    <br>
                    <br>
                    But with the national ePayment Gateway launched by the Information 
                    Technology Authority (ITA), EFT in Oman has reached the next 
                    level of maturity. Now both the public and private sector 
                    can accept and make Internet based payments. Any public sector 
                    entity or a company registered in Oman can become a member 
                    of this payment gateway.<br>
                    <br>
                    <strong>Plethora of possibilities</strong><br>
                    In eCommerce, where in trade occurs beyond borders through 
                    the ubiquitous new medium called the Internet, it is vital 
                    for businesses and customers to be able to pay for products 
                    and services online. This completes eCommerce with electronic 
                    exchange of payment instructions and settlement. Now, true 
                    eCommerce can take place in Oman and the public sector can 
                    offer e-services through the Internet and public can pay online 
                    too. <br>
                    <br>
                    This also makes the e-purse proposal for using National Identity 
                    smart cards to hold cash credit feasible. True mobile commerce 
                    with WAP websites offering catalogues and shopping carts can 
                    now connect to the ePayment Gateway and settle payments. M-commerce 
                    model involving small amounts of credit held in cards can 
                    now take off in a big way with more participating outlets 
                    and ability to register and encash loyalty points online. 
                    With the integration of e-services through a common gateway, 
                    it will soon be possible to verify the authenticity of the 
                    customers online through the national ID. <br>
                    <br>
                    <strong>ePayment Gateway of Oman</strong><br>
                    The Information Technology Authority (ITA) has recently opened 
                    up an Internet Payment Gateway for adoption both by the public 
                    and the private sectors. ITA has taken up the strategic initiative 
                    to drive the development of e-Payments in Oman. The objective 
                    is to implement an efficient and effective e-Payment infrastructure 
                    to support these e-Government initiatives as well as future 
                    e-Commerce development.<br>
				<br>
                    The ePayment Gateway is built on MasterCard Internet Payment 
                    Gateway Services (MIGS) and facilitates customer’s bank to 
                    transfer funds to merchant’s acquiring bank. A secure payment 
                    server acquires e-Payment and seeks an authorisation of payment 
                    from the customer’s bank. MIGS accepts all international credit 
                    cards, debit cards including Visa, MasterCard, Amex and Diner 
                    cards. The gateway has been tested for resilience with public, 
                    private and retail sector entities, resulting in zero-defect 
                    processing of over 5000 transactions in the first five months. 
                    To connect to this gateway entities can apply for membership 
                    through ITA. Corporate service providers of Oman, need to 
                    have a website online with catalogue and shopping cart applications. 
                    Entities having a website that can process orders online can 
                    also utilise the gateway.<br>
                    <br>
                    In a unique model, Oman’s Payment Gateway securely integrates 
                    merchant websites with the financial systems within minimum 
                    number of hopping points to increase both convenience and 
                    security. The payment server doesn’t share customer’s details 
                    with the merchant’s systems except for payment requests and 
                    approvals. The system is reliable, secure and future proof 
                    with payment card industry standards. The scope of this gateway 
                    will now extend to include account transfers and mobile payments 
                    in its next phase. <br>
                    <br>
                    The ePayment Gateway, can completely change the way we pay. 
                    For example, Ministry of Sports Affairs has utilised the payment 
                    gateway to sell tickets to football competitions online. Both 
                    OmanMobile and Nawras have enabled online payments for all 
                    their registered subscribers. The subscribers can register 
                    online for free, track their accounts details and pay with 
                    their debit/credit cards through the Internet. Once the payment 
                    is complete, an email and or SMS is sent for confirmation 
                    of the transaction. Customers pay no extra fee for making 
                    online payments. Only the merchant needs to pay a one-time 
                    joining fee, annual merchant maintenance fee and a nominal 
                    fee per transaction. <br>
                    <br>
                    <strong>Number of advantages</strong> <br>
                    Businesses that are looking for leading edge can offer better 
                    customer service by enabling Internet-based payment options. 
                    With a well developed catalogue and shopping cart services 
                    they can reach potential customers even across borders. They 
                    widen their client base without having to invest in physical 
                    infrastructures or staffing. Settlement of payment through 
                    debit/credit cards in real-time through a payment gateway 
                    reduces settlement risks and minimising the time-based credit 
                    risk to online traders. Government institutions can truly 
                    extend their services to the public at their convenience in 
                    terms of place and time. They can now avail of services through 
                    the Internet from anywhere and at anytime without having to 
                    visit the physical premises. With better efficiency and administrative 
                    cost reduction, the public sector can serve more effectively. 
                    They can compete in quality of service offering a secure payment 
                    processing to world-class standards. <br>
                    <br>
                    Customer-the king gets to be served like a king: services 
                    at the click of his mouse and payments also accepted online. 
                    This is true anytime, anywhere, fast and flexible service 
                    that they simply deserve. With the electronic transactions 
                    law in place, use of electronic signatures, communications 
                    and documents is now legally valid in Oman. Oman’s financial 
                    institutions are constantly enhancing infrastructure for security 
                    and prudent efforts are made to move towards chip-based cards 
                    and upgraded ATMs. All this assures adequate level of security 
                    in making electronic payments for day-to-day operations.<br>
                    <br>
                    <strong>Caution around the corner</strong> <br>
                    Despite some skeptics view that the scenario is not adequately 
                    ready for online payments, reality has proved that once people 
                    perceive the viability and benefits they will adopt it very 
                    quickly. With major players like mobile services providers 
                    and few other public and service sector organisations going 
                    online, the digital culture is sure to have a catalytic effect 
                    within the community in a couple of years. With well designed 
                    websites and thoroughly planned computing infrastructure, 
                    merchants can handle large number of transactions even with 
                    existing Internet speeds. Several key projects on the anvil 
                    to increase PC penetration and Internet penetration such as 
                    broadband services will further activate the pace. <br>
                    <br>
                    The gateway is built with secure encryption facilities. Membership 
                    criteria also ensure that the merchant websites have adequate 
                    security systems built within. Customer shop online and payment 
                    is processed via a Secured Socket Layer enabled session. Transactions 
                    sent from merchant’s shopping cart applications to the MIGS 
                    Payment Server are channel encrypted to prevent alteration 
                    in transit. MIGS supports ‘MasterCard – Secure Code’ authentication 
                    initiative and the ‘Visa-Verified by Visa’ authentication 
                    initiative. So with multiple layers of security implemented, 
                    it is safe to transact online through this payment gateway.<br>
                    <br>
                    Security is everyone’s responsibility and customers need to 
                    be security conscious while transacting online (See box - 
                    Online Security Tips). They must transact with reputed websites 
                    and confirm authenticity before giving payment instructions. 
                    Any online payment related doubts and queries must be directed 
                    towards either the merchants or the card providers. <br>
                    <br>
                    <strong>Poised for progress</strong><br>
                    Organisations treading the information super highway are truly 
                    empowered to complete their entire suite of business transactions 
                    online. Efficient payment systems within a country promote 
                    financial stability and the more transparent, secure and robust 
                    these systems are, it is less risky and more efficient for 
                    all parties involved. The launch of the national ePayment 
                    Gateway is a milestone achievement for the country that aspires 
                    to harness technology and triumphantly stride towards realising 
                    a knowledge-based economy.<br>
