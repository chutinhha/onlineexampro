                      <td width="880" colspan="3" height="34" bgcolor="#4078A9"><b> 
                        <font face="Verdana" size="1" color="#FFFCE8">&nbsp;How 
                        would you rate customer service standards in Oman?</font></b></td>
  </tr>
  <tr>
                      <td width="429" height="96" valign="top"> <p align="justify" style="margin-left: 5; margin-right: 5"><font face="Verdana" size="1">Good 
                          customer service is the lifeblood of any business. The 
                          essence is to send away happy customers and bring them 
                          back to your business by giving them a friendly shopping 
                          experience. Although there are promising signs of better 
                          customer service in Oman we are still surrounded by 
                          examples of disappointing, frustrating and occasionally 
                          shocking service. The biggest misconception is that 
                          customer service is generally judged by the actions 
                          of persons at a check out or somebody performing a sales 
                          function. Customer service encompasses much more than 
                          a just a friendly person performing a sales function 
                          somewhere. Yes, in Oman we need to do a lot more to 
                          reach a satisfactory level of customer service.<br>
&nbsp;</font></td>
    <td width="1" height="96" bgcolor="#BCBEC0"></td>
                      <td width="431" height="96"> <p align="justify" style="margin-left: 5; margin-right: 5"><font face="Verdana" size="1">We 
                          should be careful not to import standards that expats 
                          in particular would expect in their own countries. It 
                          is difficult for staff who have never been outside Oman 
                          or have not experienced the level of customer awareness 
                          that exists in some other countries to reproduce an 
                          experience they have never seen or experienced for themselves. 
                          Customer service is not just about smiling and saying 
                          “have a nice day”, it is also about being able to handle 
                          the specific requirements of the customer. Usually poor 
                          customer service is the result of staff not being able 
                          to deal with the specific enquiry, which then leads, 
                          almost inevitably, to an unsatisfactory exchange with 
                          the customer, and a poor experience from a customer 
                          care perspective.<br>
&nbsp;</font></td>
  </tr>
  <tr>
                      <td width="880" colspan="3" height="25" bgcolor="#D64F30"><b> 
                        <font face="Verdana" size="1" color="#FFFCE8">Are people 
                        in the first line of interaction usually aware and equipped 
                        to render the best possible service?</font></b></td>
  </tr>
  <tr>
                      <td width="429" height="104" valign="top"> <p align="justify" style="margin-left: 5; margin-right: 5"><font face="Verdana" size="1">Yes 
                          and no! In Oman we have so many people working in the 
                          first line of interaction for so many years. These people 
                          understand the pros and cons in dealing with customers 
                          and their needs. However, we also have a new crop of 
                          emerging people entering businesses in this capacity 
                          in the Sultanate. These people are in need of mentoring, 
                          teaching and coaching in order for them to learn the 
                          details of customer service. </font></td>
    <td width="1" height="104" bgcolor="#BCBEC0"></td>
                      <td width="431" height="104"> <p align="justify" style="margin-left: 5; margin-right: 5"><font face="Verdana" size="1">Many 
                          businesses and organisations in Oman are in an early 
                          stage of development stage, and training staff on how 
                          to deal with the thousands of different issues customers 
                          can bring to them takes time. There are numerous occasions 
                          when an organisation does not have a process for dealing 
                          with issues raised by customers and the front line staff 
                          finds it therefore unable to answer to their queries 
                          satisfactorily. So it is important for organisations 
                          to set up clear processes which the front line staff 
                          can bring into use, to satisfy the customer that their 
                          enquiry or concern will be properly addressed.<br>
&nbsp;</font></td>
  </tr>
  <tr>
                      <td width="880" colspan="3" height="32" bgcolor="#697C3A"><b> <font face="Verdana" size="1" color="#FFFCE8">&nbsp;What 
                        are your suggestions on improving service standards in 
                        Oman?</font></b></td>
  </tr>
  <tr>
                      <td width="429" height="82" valign="top"> <p align="justify" style="margin-left: 5; margin-right: 5"><font face="Verdana" size="1">I 
                          tend to look at this aspect in a holistic way and would 
                          ask myself the following questions when I evaluate this 
                          aspect of any company: How does the outside of the building 
                          (business) look like? Are there enough parking, refuse 
                          bins, how is the parking area marked? What is the general 
                          ambience of the room/ shop/ restaurant/ supermarket? 
                          Am I greeted cordially inside the business premises 
                          etc? The customer trusts the company to maintain these 
                          services seven days a week, four weeks a month and 12 
                          months of the year. </font></td>
    <td width="1" height="82" bgcolor="#BCBEC0"></td>
                      <td width="431" height="82" valign="top"> 
						<p style="margin-left: 5; margin-right: 5" align="justify"><font face="Verdana" size="1">Apart 
                          from ensuring that front line staff have as much training 
                          as possible, organisations need to set up an infrastructure, 
                          so that they have the option to bring up these issues 
                          within their organisation, in a way which the customer 
                          feels that though the person they are talking to cannot 
                          help, their issue will still be addressed. Only when 
                          front-line staff feels relaxed and comfortable, will 
                          they be able to deal with any situation. It is also 
                          important to choose staff for “front-line” customer 
                          care, who have the temperament and the personality to 
                          deal with challenging situations without becoming part 
                          of the problem themselves.</font></td>
