                </font></b><font face="Verdana" size="1"><b>‘Do Something 
                Different’ is an account that new and old entrepreneurs can use 
                as a ready reckoner. By Nidhi Chandola</b><br>
                <br>
                <img border="0" src="bc.jpg" align="left" hspace="8" vspace="6" width="179" height="388">Henry 
                Ford wanted to find a quicker way to assemble cars. The system 
                at that time brought workers to the parts and materials. Ford 
                reversed the system and made the parts to be sent to the 
                workers. The result was the assembly line. It became possible to 
                turn out cars at a much faster rate and thereby bring the price 
                down. The discovery was a landmark and led to the birth of a 
                massive automobile industry. This is one of the instances that 
                Jurgen Wolff provides in his book as an example of how the rules 
                of the game can be changed with out-of-the box thinking. <br>
                <br>
                <b>Roller coaster ride</b><br>
                The world of business is unpredictable with its own set of highs 
                and lows. And it is precisely to negotiate such turbulences that 
                ‘Do Something Different’ comes in handy. With its interesting 
                real life strategies which business tycoons have benefited from 
                over the years, the book is a sure shot bet to increase your 
                marketshare. Business is never the same as it was some years 
                back. In today’s scenario of cut-throat competition and numerous 
                ideas floating in the market, this is the book, which takes one 
                on a journey of self-discovery. The book provides the reader 
                with 14 well thought out and effective principles to sell 
                yourself, your product or service to customers, as they are the 
                ones who give you an opportunity to make money. These principles 
                are brought across through individual chapters that are suitably 
                supported with more than 100 anecdotes. Wolff lays a plate of 
                ideas to identify the type of customers who have never had a 
                taste of your product and how to invigorate them into taking 
                your offer. Knowing your customers fancies is something that is 
                the pronto. Accordingly, one has to redefine oneself to cater to 
                the interests and needs of the untapped customer. If one is able 
                to customise the buyer one captures the field. Not only the 
                buyer, it is also your team who need to be x-rayed for 
                brain-ticking ideas. Wolff gives an insight into the ways of the 
                trade. He raises many questions that will regale the preset 
                minds and make them envisage new possibilities confidently.<br>
                <br>
                Sometimes one is burgeoning with the desire to start a business 
                but finds himself standing at a crossroads. It is at such a 
                stage that Wolff’s book leads one. Its various anecdotes propel 
                your mind into action. Very tangily written this prospectus on 
                business expertise will certainly prove to be the Bible for 
                sprouting entrepreneurs. The author has taken real instances 
                from all walks of life and provided the reader with ideas on how 
                to get down to starting a business, how to boost sales and even 
                applying for a job. The book also given a list of ready 
                references of books and magazines to help one find resources. </font></p></td>
