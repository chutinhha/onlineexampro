            announced at the 5th Pan Arab Web Awards, staged recently in Dubai. 
            This is the first time this prestigious award has been presented. 
            Organisers said Cormack was chosen for his outstanding achievements 
            which has seen Nawras transform mobile telecommunications in the 
            Sultanate of Oman. Receiving the award, Cormack thanked the judges, 
            saying, “This award is an honour which I gratefully accept on behalf 
            of everyone in the Nawras team. When this company began it was with 
            just 250 people, 235 of whom had never worked in a mobile company 
            before.” Nawras has been delivering great value, pleasingly 
            different service and excellent quality to customers for more than 
            four years. The company began by promising to always ‘say what we do 
            and do what we say’ and has consistently continued to do that even 
            as customer numbers have climbed to over 1.6 million. “It is a 
            tribute to the kind of people they are and the energy they invested 
            in their training that we had the smoothest launch I have ever seen 
            anywhere. Today, we have a team of more than 650 people and they are 
            the finest telecommunications professionals I have ever worked with. 
            I dedicate this award to them” says Cormack. As ‘Best Telecom Brand 
            Oman’ and with the ‘Telecommunication Man of the Year’ leading the 
            team, Nawras is sure to continue to provide an award-winning mobile 
            experience for customers every day. </font></p>
