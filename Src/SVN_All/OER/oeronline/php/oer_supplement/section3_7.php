        <p align="justify"><br>
        <b><font face="Verdana" size="4">Mobile Handsets Market on a High</font></b></p>
        <p align="justify"><font face="Verdana" size="1">By Ulrike 
        Lemmin-Woolfrey<b><br>
        <br>
        Closely following the global trends, Oman’s mobile handset markets is 
        moving towards more expensive and feature rich trendy handsets from the 
        global market leader Nokia and challengers like Motorola, SonyEricsson, 
        LG and Samsung</b></font></p>
        <p align="justify"><font face="Verdana"><b>A</b></font><font face="Verdana" size="1"> 
        recently published study by Gartner states that last year more than 815 
        million mobile handsets were sold to consumers, an increase of some 21.0 
        per cent from 2004, with sales coming from new markets as well as 
        existing customers upgrading their old models.<br>
        <b><br>
        Market leaders<br>
        </b><br>
        Not surprisingly, it is the big names that took the lion’s share
        <img border="0" src="sony_ericsson.jpg" align="right" width="150" height="189">of 
        the market, with Nokia, Motorola, LG, Samsung, Sony Ericsson and Siemens 
        sharing 79.4 per cent of the global market in 2005. Of those mobile 
        giants, Nokia alone sold 265.61 million mobile handsets, increasing 
        their share of the market to 32.5 per cent, up from 30.7 per cent in 
        2004.<br>
        <br>
        Motorola, from the United States, sold 144.92 million mobile phones, 
        giving it a 17.7 per cent market share, and sliding it into second place 
        worldwide, although the company is still the most popular handset 
        producer in the States, ahead of Nokia.<br>
        <br>
        Korean Samsung is globally in third place, having sold 103.75 million 
        mobile phones, and with their market share of 12.7 per cent having only 
        marginally increased by 0.1 per cent from 2004.<br>
        LG and Sony-Ericsson represent a market share of 6.7 per cent and 6.2 
        per cent respectively, with more than 50 million handsets sold by each, 
        and both seeing an increase from the previous years. German company 
        Siemens however saw a decrease in its market share, with only 28.59 
        million mobile phones sold, representing almost half of the sales from 
        the previous year, according to Gartner.</font></p>
        <p align="justify"><font face="Verdana" size="1">Globally, most sales 
        were made in the Asia-Pacific region, where 204 million handsets were 
        sold, 164 million were sold in Western Europe, in North America sales 
        were 148.4 million, in South America 102 million, and the industry sold 
        a combined 153.5 million mobile phones to the Eastern European, Middle 
        Eastern and African market — but with the Middle Eastern market being 
        the fastest growing market in the world, this figure will be steadily 
        increasing whilst others, in North America and Western Europe have 
        virtually reached saturation point.</font></p>
        <p align="justify"><font face="Verdana" size="1"><b>LATEST TRENDS<br>
        </b><br>
        The world’s largest technology fair, Cebit, held in Hanover, Germany in 
        March 2006, was the launch site of many a new handset and technology 
        available in ever smaller ways. According to the fair’s statistics, 
        convergence is leading mobile phone users to TV applications being 
        available in the very small screen. <br>
        <br>
        At Cebit, Nokia exhibited the N92, to be launched later in 2006, which 
        has a a DVB-H (Digital Broadcasting for Handhelds) tuner built in, and 
        LG launched its V9000 TV playing mobile, with a screen that can be 
        adjusted by 90 degrees, making it suitable for watching TV. Benq-Mobile, 
        which took over the mobile arm of Siemens, and Samsung, also had 
        handsets on show that allowed users to watch TV.<br>
        <br>
        <img border="0" src="sony_ericsson1.jpg" align="left" width="150" height="106">The 
        next most popular applications available on handsets were the 
        music-download, storage and listening facilities. Samsung’s SGH i310 has 
        a hard drive that can hold some 2,000 music tracks, whereas Sony-Ericson’s 
        W950 walkman phone has four gigabytes of flash storage for music — but 
        it does not have an inbuilt camera, as compared to its K800 and K790 
        camera phones, which are acknowledged to be as good as any small digital 
        camera. This apparent trend of not making every single application 
        available on every handset seems to be consistent throughout the 
        industry, giving customers a choice of buying a phone that suits their 
        individual need, instead of offering too many applications and too 
        little capacity to use any of them properly.<br>
        <br>
        Saying that, the fashion-conscious were catered for increasingly as 
        well. Believe it or not, there are still people who simply want to use 
        their mobile phone to talk to others on the go, and want the handsets to 
        look good. For those people Nokia’s new L’Amour collection and 
        Motorola’s Razr phones are perfect, as they may not offer every single 
        application there is, but still are immensely popular because of their 
        good looks.</font></p>
        <p align="justify"><font face="Verdana" size="1">Whether it is looks, 
        MP3 players, email, or SMS — mobile phone makers are forever trying to 
        be ahead of the newest trends.<br>
        <br>
        In a recent survey, Siemens surveyed more than 5,300 mobile phone users 
        in eight countries about new wireless applications and their 
        expectations with respect to the content and functionality of these 
        applications. The survey on “innovative wireless services” was conducted 
        in Brazil, Canada, China, Germany, Italy, Korea, Russia and the United 
        States and found that consumers across all countries and continents are 
        keenly interested in mobile e-mail access — on average, 74 percent of 
        the surveyed wireless users want to be able to send, receive and edit 
        e-mails on their wireless devices. <br>
        <br>
        In many countries, Mobile TV is one of the most attractive applications 
        — with an average of 59 per cent of all respondents expressing interest 
        in utilizing this application. In Korea, a country in which Mobile TV is 
        already being offered, more than 90 per cent of all respondents voiced 
        their interest in mobile television. <br>
        <br>
        The study showed further that mobile music downloads numbered among the 
        three most popular applications, as well as Mobile Gaming, IMS-based 
        services Group Communication, Enriched Voice Calling and File Sharing, 
        which the survey found were services that young subscribers have on 
        their wish lists for new wireless applications. <br>
        <br>
        <b>Trend in Oman<br>
        </b><br>
        So what sells best in Oman? In line with the rest of the world, Nokia is 
        the overall winner in popularity, and sales. B. Kelly, Manager 
        Procurements of Modern Electronics House, puts this down to its easy to 
        use applications. “Nokia handsets are easy and straight forward to use. 
        Also, customers can get many accessories, such as interchangeable 
        covers, hand-free sets and all sorts of fancy additions, which other 
        phones do not offer in this country.” <br>
        <br>
        Mohammed Anis, Sales Manager of Jazeera Electronics, who also puts Nokia 
        as his best-seller adds: “Nokia is launching new mobile phones nearly 
        every month and customers like that. They can constantly update their 
        phones, always have the newest model. Current favourites are the 6270, 
        6280, N70, N90, but Omani customers are very well informed and are 
        already asking for the new models such as N80, N71, N91 and N92.”<br>
        <br>
        <img border="0" src="sony_ericsson2.jpg" align="right" width="150" height="167">He 
        adds that one of the other huge advantages Nokia has ove the other 
        handsets, is its resale value: “A Nokia retains much of its value, so 
        when customers want to update their model, they get a good trade-in. 
        Nokia is like the Toyota of mobile phones.”<br>
        <br>
        Second favourite in Oman is Sony-Ericson as compared to Motorola in the 
        rest of the world’s sales ranking, closely followed by Samsung, but 
        Motorola, especially the Razr model, is closely behind. <br>
        <br>
        Do women and men go for different things? Mohammed Anis confirms the old 
        stereo-types: “Men tend to know about all the new applications and are 
        updating their handsets to get the more complicated models. They want 
        the gadgets. Women tend to go for the smaller models, with exchangeable 
        covers, and more appealing designs, but camera applications are a 
        favourite with everybody.”<br>
        <br>
        Applications that sell really sell mobile phones currently are the 
        camera option, with resolutions becoming better with every models coming 
        on the market; Bluetooth; internet connectivity; music phones, such as 
        Sony-Ericson’s walkman phone, and Samsung with its built-in hard disc, 
        allowing users large MP3 storage, and increasingly: movie recording.<br>
        <br>
        B. Kelly suggests that Oman is still catching up behind more 
        mobile-savvy places such as Dubai: “In Dubai customers are heavily into 
        next generation phones, with applications such as video-conferencing 
        being very popular. Oman is not quite there yet, but our customers know 
        what they want and demand is heading that way here as well.”</font></p>
