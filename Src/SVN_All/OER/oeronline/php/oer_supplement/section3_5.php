        <p align="justify"><br>
        <font face="Verdana" size="4"><b>The New Kid Starts with a Big Bang<br>
        <br>
        </b></font><font face="Verdana" size="1"><b>The one year-old telecom 
        service provider, Nawras, has given an excellent start to the 
        Sultanate’s liberalization policy in the telecom services sector</b><br>
        <br>
        <img border="1" src="nawras_stall.jpg" align="right" width="150" height="96"></font><font face="Verdana"><b>J</b></font><font face="Verdana" size="1">ust 
        one year old, Nawras has already proven itself as a grown-up player in 
        the field of mobile communications in Oman. Launched in March 2005, the 
        consortium headed by Qtel of Qatar, and a number of Omani investors of 
        which most are pension funds, beat some 28 internationally renowned 
        competitors, amongst which were heavy weights such as Orange and 
        Vodafone, to win the second license in Oman to operate mobile telephone 
        services.<br>
        <br>
        Nawras sets everything on listening to its customers and all services 
        currently available have been launched because months of dedicated 
        research showed that there was genuine demand and need. Believing that 
        the customer comes first is reflected in many ways, even the way the 
        sales outlets and head office are set out — there are no private 
        offices, even for the CEO, Ross Cormack, who shares his office with most 
        of the management team. Space is given to the areas where Nawras staff 
        interacts with their customers, and the customer services call centre is 
        given extra attention, as, according to Cormack: ‘they are doing the 
        most important job of all: listening and talking to our customers.’<br>
        <br>
        <img border="1" src="newkid_1.jpg" align="left" width="100" height="152">Having 
        set out to offer special services such as Call Forward, Caller 
        Identification, Call Wait/Call Hold, Missed Call Notice, Voice Mail, SMS 
        and Picture Messaging as standard instead of charging extra for each, 
        Nawras is keen to offer ‘firsts’ to its customers, be they prepaid or 
        postpaid customers. Nawras was the first to introduce services at the 
        enhanced data speed (EDGE), enhanced prepaid roaming (CAMEL) with UAE 
        and Saudi Arabia, data roaming with several GCC countries and credit 
        transfer for prepaid customers.<br>
        <br>
        In just one year Nawras has hit three milestones in customer numbers, 
        with its 100,000th customer announced barely three months after launch 
        in July and the 200,000th customer was signed up in November. To date 
        Nawras has reached an impressive 300,000 mobile customers and is still 
        growing continuously.<br>
        <br>
        <b>Mission and vision<br>
        </b><br>
        • Committed to enhancing the customer’s daily life by making mobile 
        communication easier, more versatile and always a rewarding experience.<br>
        • To enrich the lives of people in Oman through better communication.<br>
        • Nawras’ mission is to be the best communications provider and employer 
        of choice in Oman.<br>
        • Nawras strives to meet its customers’ ever-changing needs in 
        communication, with its core values being to deliver caring, excellent 
        and pleasingly different services.<br>
        • Intent on offering customers Best Value, Best Service and Best 
        Quality.<br>
        <br>
        <b>Branding<br>
        </b><br>
        The three key elements of the Nawras identity are the logo, the colors 
        and the typography. The logo is inspired by the wing of the seagull or 
        the Nawras, in full flight. The energy and beauty of motion is captured 
        in simple design form. The wing in motion is symbolic of reaching out 
        and manifests the freedom that world-class wireless communication and 
        service brings to a consumer. The wing in upward motion is also 
        indicative of the sense of excellence that will drive the brand’s 
        growth.<br>
        <br>
        The spectrum of colours from the greenish yellow hue to the blue, adds 
        another dimension; the qualities of being innovative, successful, 
        contemporary and professional. The colours signify the portfolio of 
        diverse products and services that Nawras will bring to the different 
        market segments. The combination of colours also serves to make the logo 
        unique. Together, they visually embody the Nawras Corporate value of 
        being pleasingly different. Finally, the typography is unique and gives 
        the brand a modern, yet warm and inviting effect.<br>
        <br>
        <b>Roaming and coverage<br>
        </b><br>
        <img border="1" src="newkid_2.jpg" align="right" width="150" height="96">When 
        launched, Nawras had agreements with just five international roaming 
        partners in place. To date some 120 agreements have been signed, 
        covering 70 countries. Roaming is available in 95 per cent of Omani’s 
        favourite travel destinations, including most of the destinations on 
        both Oman Air and Gulf Air’s route networks.<br>
        <br>
        In the first year of operation, Nawras placed great emphasis on 
        expanding its own network and delivering national coverage to customers 
        as soon as possible. At launch more than 62 per cent of the Omani 
        population was covered by the Nawras mobile broadband network. The 
        Nawras mobile broadband network now covers more than 80 per cent of the 
        Omani population. Currently with national roaming agreement Nawras 
        offers coverage to nearly 95 per cent of the population at Nawras 
        prices! With the new coverage in Al Amerat and Quiryat, Nawras now 
        promises to provide the widest coverage in the Sultanate.<br>
        <br>
        <b>Customer Service</b><br>
        This being the number one priority of Nawras, customers can call 
        Customer Service 24 hours a day — 7 days a week. Customers can access 
        their bills online on www.nawras.om and see their full call and message 
        detailed-uniquely this service is available for prepaid as well as 
        postpaid customers. For paying the bill customers cannot only go to one 
        of the 13 Nawras sales outlets, but also pay bills with two associated 
        banks, either at the local bank branch or via ATMs.</font></p>
