        <p align="justify"><font color="#000000" face="Verdana" size="2"><b><br>
        </b></font><b><font color="#000000" face="Verdana" size="4">New Era for 
        Telecom<br>
        </font><font color="#000000" face="Verdana" size="1"><br>
        </font></b><font color="#000000" face="Verdana" size="1"><b>The 
        liberalization and privatization became a reality for telecommunications 
        in Oman throwing up a sea of opportunities for the corporates, investors 
        and customers alike</b><br>
        <br>
        ‘</font><font color="#000000" face="Verdana"><b>T</b></font><font color="#000000" face="Verdana" size="1">he 
        freedom to communicate the feelings never felt so good’. The period 
        2005-2006 has transformed the telecommunications industry in Oman 
        tremendously. Though Oman’s entry in World Trade Organization (WTO) in 
        2001 had heralded the era of competition but the liberalization and 
        privatization became a reality in 2005 only. Telecommunications was no 
        more a public service provided by the government for the welfare of the 
        population. It transformed into a profit-oriented service to increase 
        telecom consumerism in the society.<br>
        <br>
        Though the telecom market in Oman has been growing at a very fast rate 
        but it is still one of the least developed in the GCC region. The 
        telecom industry in Oman consisted of the government owned Omantel only 
        until the award of the second mobile license to the second mobile 
        operator in February 2005. Omantel is currently the sole provider of 
        fixed line, Internet and data telecommunications service. It got listed 
        on Muscat Stock Market (MSM) in August 2005 after a highly successful 
        IPO (initial public offering). It had entered the capital market with an 
        IPO of 225 million shares at a price of RO1.280 per share aggregating to 
        RO288 million on 11 June 2005. The government divested 30 per cent of 
        the company’s paid-up capital of RO75 million. Overall, 157.5 million 
        shares were offered to Omani individual investors, and 67.5 million 
        shares to specified pension funds and charitable organisations. The 
        company declared a maiden cash dividend of 70 per cent for 2005.<br>
        <br>
        Omantel’s subsidiary company, Oman Mobile (in which Omantel owns 99 per 
        cent shares) and the new entrant Nawras (a joint venture between Qatar’s 
        Q-Tel, Denmark’s TDC and prominent Omani partners) are the two players 
        in the mobile (GSM) segment. Oman Mobile (incorporated on 1 March 2004) 
        enjoying the first mover advantage dominates with more than 80 per cent 
        market share. Nawras, the new kid on the block, has captured rest of the 
        market in its first year of operations.<br>
        <br>
        <b>Mobile wave<br>
        </b><br>
        Meeting the objectives of the liberalization policy, the introduction of 
        mobile services by Nawras has resulted in a major expansion of both 
        market size as well as portfolio of services for the customers. In the 
        last one year, the number of mobile subscribers has jumped to more than 
        1.5 million resulting in a growth of over 80 per cent. The prices have 
        tumbled down to an all time low and host of new services on mobile 
        platform have been launched encouraging the mobile users to talk more.<br>
        <br>
        When Nawras was launched on 16 March 2005, the market was expecting that 
        the going will get too tough for Oman Mobile as it will no longer be 
        able to enjoy its monopoly in the market. Proving the skeptics wrong, 
        Oman Mobile successfully managed to meet the stiff competition from 
        Nawras and in the process registered more than 50 per cent subscriber’s 
        growth. From 806,280 subscribers as on 31 December 2004, Oman Mobile has 
        raced to almost 1.2 million plus subscribers as on 28 February 2006. 
        Today Oman Mobile offers a wide range of services including Hayyak 
        (prepaid), Mada (postpaid), Taif (General Packet Radio Service/2.5G 
        broadband), GSM fax and data services, SMS, MMS, roaming across 105 
        countries and Thuraya connections. The company has introduced number of 
        innovative tariff packages and value added services to make mobile usage 
        more affordable and exciting.<br>
        <br>
        Nawras, on the other hand, has put up a good show in its first year of 
        operations by attracting over 300,000 subscribers despite aggressive 
        marketing from Oman Mobile. The company offers its customers Mousbak &amp; 
        MousbakPlus (prepaid), Ajel (postpaid), Edge (broadband), SMS, MMS, 
        roaming across 70 countries etc. Nawras has already invested close to 
        US$140 million and is committed to further investment of US$1.5 billion 
        to expand its services in the next eighth years.<br>
        <br>
        Both the operators reach over 95 per cent of the population in Oman 
        currently. In areas where Nawras doesn’t have the required 
        infrastructure currently it uses Oman Mobile’s networking. On the 
        regulations front, Oman Mobile got the license in 2004 to operate mobile 
        services for the next 15 years. Nawras paid the government one-off 
        payment of US$ 104 million to obtain the license. The mobile license 
        imposes an annual royalty of 12 per cent of the gross revenues. Telecom 
        Regulatory Authority (TRA) set up in March 2002 as a corporate body to 
        regulate the telecom market, is authorized to introduce the third mobile 
        operator after the completion of three years of operations by Nawras.<br>
        <br>
        <b>Fixed line<br>
        </b><br>
        Currently, Omantel enjoys complete monopoly as the sole provider of 
        fixed line telecom services in the Sultanate. As on January 2006, there 
        were 255,099 fixed lines in use. Omantel got listed on MSM last year 
        provides services including fixed prepaid home phone (Sahl), fixed 
        prepaid (Jibreen), smart card payphone (Al Multaqa), toll free services, 
        VSAT and marine radio. It also provides international call (Worldcall) 
        and national call services. Omantel’s international prices are heavily 
        influenced by the inter-settlement prices in the country of termination.<br>
        Oman has a fixed line penetration of just 10.9 per cent which is very 
        low compared to other states within the GCC region. Omantel has taken 
        initiatives like free incoming calls for prepaid fixed line etc. to 
        increase penetration. Omantel was awarded the fixed line license for 25 
        years in 2004. TRA has already taken active steps to introduce the 
        second fixed line operator (private). As per market expectations, the 
        second operator might launch its services in the beginning of next year. 
        The second operator is expected to lead to more service choice and 
        drastic transformation in the rate structure for the benefit of the 
        customers.<br>
        <br>
        <b>Internet and data segment</b><br>
        <br>
        <img border="1" src="fixed_line.jpg" align="left" width="182" height="100">Just 
        like the case in fixed line services, Omantel is also the sole provider 
        of Internet and data services in the Sultanate. Omantel currently 
        provides Internet dial-up (Omannet), Internet prepaid (Al Ufuq), 
        Internet roaming, web hosting, advertising and cyber café services. It 
        also offers ISDN and broadband ADSL (PowerNet) services, national and 
        international leased circuits (ATM, MaxNet) and managed data networks 
        aimed at corporate customers.<br>
        <br>
        The Internet penetration in Oman is relatively low compared to the 
        neighboring GCC countries. By the end of January 2006, the penetration 
        rate was below 3 per cent. This penetration figure was the reflection of 
        50,110 subscribers reached by the end of January 2006. The subscribers 
        are projected to grow at a CAGR (compounded annual growth rate) of 21.5 
        per cent between 2005 and 2009, ending the year 2009 with 185,000 
        Internet subscribers. This growth is expected to be driven by the 
        existence of more than one player in the market and by the increased 
        adoption of broadband Internet. With over 45 per cent of the population 
        below 19 years, Internet is going to be adopted by the majority of the 
        population in the next few years to drive the industry.<br>
        <br>
        The shiny days are ahead of the telecom industry in Oman with fresh 
        dosage of privatization and liberalization in the pipeline. No wonder, 
        the telecommunication infrastructure giants including Siemens, Ericsson, 
        Alcatel, Motorola and Huawei etc. are making a beeline to strengthen 
        their presence in the Sultanate.</font></p>
