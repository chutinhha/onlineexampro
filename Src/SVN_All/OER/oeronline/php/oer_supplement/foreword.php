        <p align="justify"><font color="#000000" face="Verdana" size="1">In 
        2004, Oman Mobile was set up as a subsidiary company of Omantel 
        dedicated to provide the mobile services in Oman. In 2005, the mobile 
        market was thrown open for competition resulting in the entry of the 
        private operator Omani Qatari Telecommunications Company (Nawras) as the 
        second mobile service provider. Omantel was privatized with successful 
        initial public offering (IPO) leading to 30 per cent divestment of 
        shares by the government and listing of Omantel shares on Muscat 
        Securities Market (MSM). Today Omantel leads the table of the top 
        companies (based on revenues in 2005) listed on MSM. <br>
        <br>
        <b>Stupendous mobile growth<br>
        <br>
        <img border="0" src="foreward1.jpg" align="left" width="125" height="111"></b>All 
        these initiatives taken by the government in the last few years have 
        started yielding excellent results. Today, we are proud to state that 
        Oman offers a wide range of telecommunications services based on mobile 
        and fixed networks across the regions and wilayats touching the life of 
        everyone. The healthy competition between Oman Mobile and Nawras has 
        made the mobile market more consumer friendly. Both operators are 
        striving hard to offer the best services at the most affordable prices 
        to the consumers. No wonder, the number of mobile subscribers has 
        skyrocketed in the last one year. We started in January 2005 with 
        837,856 mobile subscribers. The figure has jumped to 1.37 million by 
        January 2006 resulting in a growth of more than 60 per cent on year to 
        year basis. As we had expected, the shift from a monopoly to a vibrant 
        and competitive market has been a smooth process. TRA is to claim some 
        credit for ensuring this smooth transition as it has regulated the 
        market with a well laid out transparent rules and regulations leaving no 
        scope for any ambiguity. <br>
        <br>
        Besides widening of the subscriber base, the mobile market has taken a 
        quantum leap in terms of deployment of new technologies and the array of 
        services offered to the subscribers. The consumers can avail of a range 
        of services including broadband, MMS, wide international roaming and 
        business solutions etc. The day is not very far when 3G (third 
        generation) and number portability will also become a reality in the 
        Sultanate. <br>
        <br>
        <b>Fixed line challenge<br>
        </b><br>
        As seen already in most of the markets across globe where the mobile 
        services have grown at a very fast pace, the fixed line growth has come 
        under tremendous pressure in Oman too. The fixed line subscriber base 
        has grown marginally in the last one year. In January 2005, it was 
        pegged at 250,720. The figure has moved to 255,099 in January 2006. 
        Currently, Omantel is the sole subscriber of fixed line services in the 
        Sultanate.<br>
        <br>
        As a step towards further opening up our telecom sector, the Government 
        decided to make the fixed line segment more competitive. TRA is already 
        working on the process to introduce the second operator in this segment. 
        We are hopeful that this will lead to a wider pool of choices available 
        to the customers to make Oman a perfect-networked society. <br>
        <br>
        <b>Towards a digital society<br>
        </b><br>
        Internet and related services is another area in which we are working in 
        close conjunction with other ministries and corporates to convert our 
        vision of ‘Digital Oman’ a reality. As on January 2006, the number of 
        Internet subscribers was 50,110 in Oman. But when we talk of Internet 
        penetration and usage, we must keep one important factor in mind that 
        there are always multiple users using one subscriber line. For example, 
        in many companies, a large number of their employees use Internet for 
        their information needs. Similarly, those who are on the move or don’t 
        have access to PC, use cyber cafes to surf Internet. So though the 
        subscription rate may not be very high but there are far more Internet 
        users in Oman than what the subscription figures suggest.<br>
        <br>
        We do agree that we need to work on increasing the PC penetration in 
        Oman so that every citizen could make use of the Internet for their 
        social and economic upliftment. Besides upgrading the Internet support 
        infrastructure, the government is working simultaneously on improving 
        the Internet based services in the Sultanate. Ubar portal to act as the 
        central gateway connected to all the ministries/government offices and 
        launch of number of e-services to make e-governance more effective are 
        some of the major steps in that direction.<br>
        <br>
