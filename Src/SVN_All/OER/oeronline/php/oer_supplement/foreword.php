              <p align="justify" style="margin-right: 5px">
              <font color="#000000" face="Verdana"><b>O</b></font><font color="#000000" face="Verdana" size="1">man 
              has always recognized the important role played by 
              telecommunication in the economic and social development in the 
              Sultanate. But the first turning point in the telecommunication 
              history of Oman came in 1980 when the General Telecommunications 
              Organization (GTO) was formed to cater to the telecom needs of 
              individuals, corporates and government. Later on, GTO was 
              transformed into closed joint stock company under the name of Oman 
              Telecommunications Company (SAOC) in 1999. In the last few years, 
              especially in 2005, the telecom sector has undergone a sea change 
              to propel Oman among the frontrunners in terms of 
              telecommunications growth in the Gulf region. <br>
              <br>
              The Telecommunications Regulatory Authority (TRA) was set-up under 
              the Telecommunications Regulatory Act, which was issued under 
              Royal Decree No. 30/2002. The establishment of TRA was a step in 
              the direction to liberalize the telecom sector. </font></td>
