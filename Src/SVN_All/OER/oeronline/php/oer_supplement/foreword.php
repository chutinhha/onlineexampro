        telecom market in Oman is headed for shiny days. The international 
        telecom companies are showing a keen interest to set-up/expand their 
        operations in the Sultanate. Though, the rate at which we are going may 
        not be sustainable but we believe that we will continue to grow at a 
        very healthy rate. There are some markets in the world which have more 
        than 100 per cent telecom penetration also. We believe that our telecom 
        industry will continue to keep pace with the communications requirements 
        of the burgeoning economic development.</font></p>
