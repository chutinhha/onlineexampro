        <b>RAMADAN GOODWILL JOURNEY<br>
        </b><br>
        <img border="1" src="exec_summary2.jpg" align="left" width="150" height="98">Nawras 
        takes corporate social responsibility (CSR) seriously and has played a 
        significant role in the Omani society and is committed to further 
        enhancing the standard of livings of those most vulnerable and needy 
        people in society. Nawras has donated to various charities throughout 
        the year. The well-known of our activities was the major charitable 
        donation campaign “Nawras Goodwill Journey” was an employee initiative 
        to do something special and deliver another Nawras 1st in the Sultanate 
        during the Holy Month of Ramadan. The objective was to visit 11 
        charities in 11 wilyats of the 9 main regions all over the Sultanate, 
        covering 4700km and donate items which improve the less fortunate in the 
        society quality of life in a sustainable way. In addition, Nawras wanted 
        to raise level of CSR for companies in Oman, develop partnership with 
        Ministry of Social Development and raise awareness for various charities 
        and spend time with children in those centres in order to give back to 
        society. <br>
