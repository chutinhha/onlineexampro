        <p align="justify"><br>
        <font face="Verdana" size="4"><b>Changing Telecom Landscape<br>
        <br>
        </b></font><font face="Verdana" size="1">By Ulrike Lemmin-Woolfrey<br>
        <br>
        <b>Nawras, the &#64257;rst private telecom service provider in Oman, has made a 
        dramatic impact on the mobile market in the Sultanate with its 
        aggressive marketing and customer-service backed-up by state-of-the-art 
        technology</b></font></p>
        <div align="center">
          <center>
          <table border="0" cellpadding="0" cellspacing="0" style="border-collapse: collapse" bordercolor="#111111" width="100%" height="163">
            <tr>
              <td width="74%" rowspan="2" height="163">
              <p align="justify" style="margin-right: 5px">
              <font face="Verdana" size="1"><b>Congratulations on your first 
              anniversary. Have you managed to achieve your objectives by the 
              end of the year? <br>
              </b><br>
              What we built was entirely based on what customers told us about 
              what they wanted, plus best practice with view to our 
              shareholders, Q-tel and TDC. When we set out at the beginning of 
              the year, we said we would change the telecommunications 
              landscape. And I think we have. Everybody today is better served, 
              wherever they get the service from. We completely re-defined the 
              mobile service; we took the distance out of pricing and offered a 
              new product to the market, with a pre-paid that behaves like a 
              post-paid service. We changed distribution — we reached the place 
              near you. We changed the payment structure — you can pay at both 
              Bank Muscat, Oman Arab Bank, over the Internet, through ATMs and 
              we also totally changed the concept of customer service. </font>
              </td>
              <td width="26%" height="170">
              <p align="center">
              <img border="0" src="changing_telecom.jpg" width="96" height="131"></td>
            </tr>
            <tr>
              <td width="26%" height="1">
              <p align="center"><b><font size="1" face="Verdana">Ross Cormack,<br>
              Chief Executive Of&#64257;cer, <br>
              Nawras</font></b></td>
            </tr>
          </table>
          </center>
        </div>
        <p align="justify"><font face="Verdana" size="1"><b>So you mean to say 
        Nawras is very much on track?<br>
        </b><br>
        Absolutely! We are ahead of where we expected to be — we have more than 
        300,000 customers today. There are more pre-paid customers, but the 
        post-paid base number is starting to build up nicely since we announced 
        our progress in the last few months. We have introduced a number of 
        groundbreaking practices, and are making the same services available for 
        pre-paid as well as for post-paid, for example. We made a proposition as 
        the Sultanate’s customer-friendly network, which basically says that we 
        will offer Best Value, Best Service, Best Quality — and that’s we mean 
        to continue doing. We are just about the lowest price for most usage and 
        services, with a number of services, such as voicemail, being 
        pre-positioned, rather than having to pay extra for it. Since the launch 
        we have managed to reach some 80 per cent of the population, up from 
        just 62 per cent at launch. <br>
        <br>
        We have the leading coverage in the country with nearly 95 per cent 
        coverage, thanks to our relationship on national roaming with Oman 
        Mobile. The great thing about it is that you can use our service 
        wherever there is coverage with Nawras or our national roaming partner, 
        but at Nawras prices, which is a great deal. <br>
        <br>
        <b>What about international coverage?<br>
        </b><br>
        We are also working on dramatically increasing our international 
        coverage. When we launched we only had five roaming partners, today we 
        have 120 roaming agreements, in 70 countries. That is up from 5 to 120 
        roaming partners internationally, in just one year. <br>
        <br>
        The 70 countries cover nearly 95 per cent of where most Omanis like to 
        travel to; we roam to most of the places on Oman Air and Gulf Air’s 
        route networks. That type of development makes a big difference.<br>
        <br>
        We also have international services, which are groundbreaking. If you 
        are a pre-paid customer, we have set up a system that lets you do 
        call-back roaming in all the countries where we have roaming, so you can 
        use your pre-paid service in 70 countries. With us you can not only 
        receive SMS abroad, but also send them, which is not a usual facility 
        with most providers. We are always trying to make sure that we provide 
        our customers with something that little more advanced <br>
        and special.<br>
        <br>
        Additionally we have now launched pre-paid roaming that works like 
        post-paid roaming, in both the UAE with Etisalat, and in Saudi Arabia 
        with Mobily. A new experience!<br>
        <br>
        <b>What other new services have you introduced in Oman?<br>
        <br>
        </b>Recently, we introduced twin SIM cards with the same number, which a 
        lot of VIPs and business people have asked us for. This means you can 
        have the same number on your phone, as well as in the car, or on your 
        PDA. We have been offering conference calling from day one, which allows 
        you to talk to six people at the same time, on pre-paid and post-paid, 
        which is a great business utility. <br>
        <br>
        With the EDGE-based network we augmented PC MCIA cards which you can 
        stick in your laptop, and recently we launched a small USB device, into 
        which you can place your SIM card, plug it into the laptop and this 
        gives you mobile broadband, whether you are connected to ADSL where you 
        live or not, and whilst on the move. We spent a lot of time and money on 
        basic service, and now we are doing very well against the key reference 
        Ericsson, our strategist network partner. <br>
        <br>
        They benchmark on our behalf against 81 networks around the world, and I 
        am proud to say that we came in the top 10 for 4 out of the 8 
        parameters, which is incredible for such a young network. Nawras now 
        sits alongside some very famous networks in the world, and we are very 
        proud of that.<b><br>
        <br>
        How the human capital is making a difference to the company? </b><br>
        <br>
        We have employed the best people available in the country, chosen out of 
        over 10,000 CVs just in the first couple of months, and many more since. 
        These people have the skills and knowledge to really help our customers. 
        They are our ambassadors and I encourage everybody to bring every 
        problem to me and tell me what customers want, if there are complaints, 
        what they are and then we will work on bettering that.<br>
        <br>
        Another thing we are proud of is our offices: I share an office with all 
        the senior management team, which is unusual, but gives us a chance to 
        have discussions and multitask all together, and it means we can learn 
        quickly. We are also customer close. Downstairs are the people who talk 
        to our customers everyday, in our contact centre. There we have our 
        customer champions, and they have much more space than we have, but they 
        are doing the most important job — talking and listening to our 
        customers, and we are proud of our customer service.<br>
        <br>
        Equally we are well on our way with our Omanisation target; we launched 
        with nearly 80 per cent, and are still maintaining that. We have an 
        intention of being at least at 90 per cent within our first five years. 
        That gives us an advantage because we have really smart people who 
        understand the country they come from, and can relate to the people who 
        live here.<br>
        <br>
        <b>How far you have progressed in increasing your customer reach?<br>
        </b><br>
        <img border="1" src="changing_telecom1.jpg" align="left" width="150" height="108">We 
        have increased our reach tremendously. We had only 400 points of 
        presence at the time of our launch and today we have more than 1,000. We 
        had eight stores at launch and today we have 12. And what we have 
        achieved on the Internet is very good even compared top major networks 
        around the world: you can go to www.nawras.com and look at your present 
        bill, paid and unpaid calls up a few minutes earlier, which is a great 
        service. And we have just launched the value transfer, where one 
        pre-paid customer can transfer value to another, to top up their credit, 
        which is great for people away from home, parents crediting their kid’s 
        mobiles and businesses making certain amounts of value available to 
        their employees. <br>
        <br>
        <b>What is the current ratio of your pre-paid and post-paid customers?<br>
        </b><br>
        At present, we are at 95 per cent pre-paid, the market largely is a 
        pre-paid one, but since we started to show people that we have the 
        network and many roaming partners, the post-paid rate is increasing 
        satisfactorily.<br>
        <br>
        <b>How much time does on average a customer spends on the phone?<br>
        </b><br>
        We have approx 220-250 minutes per month per pre-paid customer and 
        between 600-650 minutes per post-paid customer.<br>
        <br>
        <b>How much investments Nawras has made so far?<br>
        </b><br>
        <img border="1" src="changing_telecom2.jpg" align="right" width="110" height="163">At 
        the end of last year, we committed some US$140 millionfor 18 months, so 
        we have now spent a lot of that, and are finalizing proposals for 2006, 
        for further investment. We are investing in more people and facilities, 
        but luckily we have some revenues to offset some of that and we are 
        hoping to break even and start into profit around the end of the second 
        year of operation.<br>
        <br>
        <b>Which services are performing best for you?<br>
        </b><br>
        The big services are the basic services, like voicemail and SMS. Out of 
        voice, there are more international calls than we expected. EDGE, the 
        mobile broadband, is used by a small number of people, but they use a 
        vast amount of gigabytes a month.<br>
        <br>
        SMS has climbed by some 25 per cent since launch, and MMS is again small 
        usage, but this is what we need to do: we offer a large number of 
        modules, and people can chose what is most useful for them.<br>
        <br>
        <b>Mobile phones are no longer used to simply talk to other people, how 
        do you think the market is going to change in the future?<br>
        </b><br>
        Mobile services are changing our professional and personal life 
        completely. For example, I use a mobile phone and an i-mate. I have got 
        my schedule on there, check my totally up-to-date emails — we have a 
        joint venture with Microsoft and i-mate — do my online banking, and I am 
        always completely informed. And between those two devices, you do not 
        need much more to stay in touch with people. At the moment this would 
        use two SIM-cards, but we are starting to think differently, namely not 
        per SIM usage, but per person usage. As we get smarter, we will start 
        tracking the per customer usage, be it pre-paid or post-paid. We have 
        set our all-you-can-eat pricing at RO39 a month, which is priced very 
        competitively with ADSL, considering you can get this on the move. If 
        you use less, we automatically calculate at a lower rate. If you don’t 
        use it you don’t pay for it. Again this is not for a high number of 
        people, but it is available, if you want it.<br>
        <br>
        To most of our customers it is not the extra services that are important 
        but the fact that with us they can make a call when they want to, and 
        get through at the time they want to get through. It is the basic 
        assurance of connectivity that is our most important service, but all 
        the extras are also there for people who would like to utilize them.<br>
        <b><br>
        What do you think of local competition?<br>
        </b><br>
        Competition is good for customers, and for business. It is extremely 
        satisfactory to see that our local competitor has launched services that 
        we innovated in Oman. It proves that we are getting it right. Generally, 
        it will still be a very fast growth market, extremely competitive. I 
        believe that the desire for communication will increase, and existing 
        customers will probably increase their usage, particularly because we 
        offer these new services, that weren’t available before. In terms of 
        customer growth I expect growth and in revenue, too.<br>
        <br>
        <b>How much growth are you looking for and what will the second year 
        bring?<br>
        </b><br>
        We were challenged by the Telecom Regulatory Authority (TRA) to grow the 
        whole market, and I think we have done a good job with that; at least 60 
        per cent of our customers have not used mobiles before.<br>
        <br>
        What we have done over the last year is to have launched services that 
        appeal to both customers of existing services and new customers; now we 
        are targeting in a more focused way, and are increasing and improving 
        our Arjel offering. For businesses we are delivering modules and 
        allowing businesses to manage their accounts over the Internet and at 
        the same time, with mobile broadband, offering very attractive new 
        services. We are going though trials with i-mate and Microsoft at the 
        moment, and trying to build our service offerings in a thoughtful way 
        that will build long-term partnerships with corporate customers. <br>
        <b><br>
        Oman has some 2.5 million people, how many mobile subscriptions can the 
        market sustain? <br>
        <br>
        </b>At the moment, there is 45-50 per cent penetration. Initial market 
        forecast research suggested that there could be 70 per cent penetration 
        in the first three or four years, but we seem to be growing faster than 
        expected. Etisalat has more than 90 per cent and two years ago Taiwan 
        went to 120-130 per cent, so there is plenty of space for growth. The 
        traditional measure of SIMs per heads can go to very high numbers, which 
        you can reach if customers want to use several devices. Oman is a fast 
        growing market, expanding economically, in tourism and has a thirst for 
        excellent services. We are here to provide that, but keeping in mind 
        that most importantly it is our job to earn our customers.<br>
