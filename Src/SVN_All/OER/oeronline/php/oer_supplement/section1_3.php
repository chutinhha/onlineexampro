        <p align="justify"><font color="#000000" face="Verdana" size="1">Despite 
        teething troubles, TRA has managed to achieve many of its objectives in 
        a short span. Be it the entry of Nawras or the number portability issue, 
        TRA has successfully managed to further expand the market and maintain a 
        healthy competition among the service providers without any major 
        dispute among them. Now it is all set to extend the liberalization drive 
        to the fixed line/ Internet business and curb the menace of cyber crimes 
        with a new law.<br>
        <br>
        <img border="0" src="competition_stimulates1.jpg" align="right" width="125" height="116">TRA 
        member, Engineer Naashiah Al Kharusi, talks on the success and current 
        activities of TRA. She was elected full-time member of the Temporary 
        Committee of TRA in June 2002. Previously, Engineer Naashiah was the 
        telecom advisor to H.E. the Minister of Transport and Communications on 
        all technical and policy matters relating to telecommunications sector 
        development. She also served as a member of the board of Omantel since 
        its establishment in 1975 until May 2002 where she served as chairperson 
        of the technical committee and as a chairperson of tariff committee. She 
        was a member of executive committee of restructuring of the telecom 
        sector and chaired the working team in the preparation of privatization 
        of the incumbent. She contributed to the establishment of the first 
        frequency coordination committee for the AGCC countries. In 1996, she 
        acted as a governor for a full year in Intelsat Board, representing AGCC 
        group in this organization. <br>
        <br>
        </font><b><font color="#000000" face="Verdana" size="2">Excerpts of the 
        interview:</font><font color="#000000" face="Verdana" size="1"><br>
        <br>
        What is the primary responsibility of the Telecom Regulatory Authority?</font></b><font color="#000000" face="Verdana" size="1"><br>
        <br>
        TRA is the overall regulatory body for the telecommunications sector in 
        the Sultanate of Oman. It lays down the basic ground rules of operation 
        — from the numbering scheme and the infrastructure related issues faced 
        by the players in both the fixed line business and the mobile segment. 
        And now as we are on the anvil of the opening up of the fixed line 
        telephone segment to competition, something that was not there till now, 
        we need to re-look the statutory aspects that will govern the 
        competition in this sector, right from pricing to issues that pertains 
        to infrastructure.<br>
        <br>
        <b>What is the role of TRA on the pricing strategy adopted by the 
        different telecom players?<br>
        </b><br>
        TRA does not set prices. Prices are set by operators/service providers. 
        TRA regulates prices, in the sense that every price proposal for 
        new/existing services is submitted to TRA for approval before 
        implementation.<br>
        <br>
        No player — be it Omantel, Oman Mobile or Nawras — is at liberty to 
        increase the prices without adhering to a formula that has been put into 
        practice with the approval of all three players and the adherence to 
        which is monitored by the TRA. This, we think, is absolutely essential 
        in the Sultanate today as we are beginning to get more competitive. 
        Mobile business is already competitive and very soon the fixed line 
        business will follow suit. One crucial responsibility of the TRA is to 
        prepare the market for healthy competition. If pricing is 
        well-regulated, it will lead to smooth operation and healthy competition 
        among the players be it fixed line or the mobile business. <br>
        <br>
        <b>Is TRA checking the quality of service provided by the operators?<br>
        <br>
        </b>TRA regularly checks the network performance and quality of service 
        of operators. Failure to comply with quality of service requirements may 
        result in penalties imposed on the service providers.<br>
        <br>
        <b>What is the extent of Internet penetration in the Sultanate? </b><br>
        This is one issue that needs close attention since the statistics that 
        we have do not reflect the true picture. One fixed line connection does 
        not mean one Internet subscriber. What I mean is there is no 1:1 ratio 
        as far as the usage is concerned. Usage is higher than the numbers that 
        we have and the main reason for this are the cyber cafes. There are 
        Internet users who do not have a fixed line but they access Internet 
        from the cafes. Thus, the statistics we have do not reflect the true 
        picture.<br>
        <br>
        <b>What is the TRA’s stand on spamming?</b><br>
        Spamming is a cyber crime. It is something that is beyond control since 
        it is done from outside and it is not possible to monitor where a sender 
        is geographically located. What is needed to stop this is to evolve 
        international consensus on how to define and determine the limits of 
        what constitutes a criminal offence with regard to spamming and what 
        falls outside of it. This is something that all countries need to be 
        conscious of and work towards so that the cyber world remains more or 
        less clean. This will only allow the citizens of each country to reap 
        the benefits that will arise of the regulated cyber world.<br>
        <b><br>
        What steps TRA is taking to stop Internet frauds?<br>
        </b><br>
        TRA recently organized a three-day workshop on combating cyber crimes. 
        The event was sponsored by the International Telecommunication Union (ITU)’s 
        Arab Center of Excellence (Arab/CoE). The workshop aimed at building the 
        requisite professional and technical capabilities to meet the challenge 
        of growing menace of electronic and cyber crimes. The participants in 
        the workshop included representatives of the Arab governments, law 
        enforcement agencies, judicial bodies, lawyers, regulatory authorities, 
        telecom operators and service providers. A wide spectrum of issues 
        related to cyber crimes like net piracy, violation of copyright and 
        intellectual property, computer viruses, cyber money laundering, impact 
        of cyber crimes on e-business were discussed. One major area of concern 
        is the legislative vacuum that exists in addressing cyber crime. The TRA 
        is in the forefront of efforts to create a suitable legislative 
        framework in Oman to deal with cyber crimes that take place in cyber 
        space, far beyond the reaches of national borders.<br>
        <br>
        The proposed Electronic Transaction and Commerce Law, currently being 
        reviewed prior to its enactment, will help provide a secure legal 
        environment for commerce via the Internet. The draft law would lead to a 
        media neutral environment for transacting business. It is intended to 
        reinforce the existing legislation governing communication and storage 
        of information which does not contemplate the use of electronic 
        transactions and commerce.<br>
        <br>
        The draft law is divided into 10 chapters. It deals with a range of 
        issues including definitions of electronic transactions, requirements 
        for effecting electronic transactions, methods of protecting 
        e-transactions and commerce, the functions and prerogatives of the 
        competent authority, consumer protection, protection of private data, 
        electronic records and signatures, and penalties prescribed under the 
        law.<br>
        <b><br>
        Do you censor websites in Oman?</b><br>
        In Oman, we do not believe in censorship of information that does not 
        contravene our culture and civilization. We follow a code of conduct 
        that anything that builds on our culture should not be censored. But 
        websites that affects children and goes against our culture are 
        censored.<br>
        <br>
        <b>What progress has been made on the opening of the fixed line 
        services?</b><br>
        <br>
        The TRA is taking steps to open up the fixed telecom services, currently 
        monopolized by Omantel, to competition. A consultant will shortly be 
        appointed to assist the TRA in the award of licenses for the fixed line 
        services covering the provision of local access, long distance and 
        international services and for public data services. Based on the 
        recommendations of the consultant, and after public consultation and 
        approval of the government, a timeframe will be announced for opening up 
        the fixed line telecom market for competition.</font></p>
