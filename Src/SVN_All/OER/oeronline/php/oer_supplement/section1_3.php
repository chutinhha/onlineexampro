                    With the changing dynamics of the market, it was felt that 
                    an independent telecom regulator, rather than a government 
                    department, would make the market more attractive to foreign 
                    investors as the market will be deemed to be less vulnerable 
                    to political interference. That’s how TRA was born under the 
                    chairmanship of His Excellency Shaikh Mohammed bin Abdullah 
