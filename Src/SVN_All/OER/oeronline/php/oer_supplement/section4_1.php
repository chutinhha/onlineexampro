        Services in Dakhelia/Dhahira and Sharqiya regions including two new MSCs. 
        Additionally, the establishment of 2 new Gateway MSC as well as GPRS and 
        MMS technology for post- and pre-paid GSM subscribers were part of the 
        project.<br>
