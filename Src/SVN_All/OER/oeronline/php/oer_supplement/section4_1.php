        </b><br>
        Siemens has completely build the very first GSM Network in the Sultanate 
        with a capacity of 100,000 subscribers in Muscat Governorate and Salalah 
        Area. The service was launched during the National Day 1996. In the next 
        years, Siemens did further GSM expansions in Salalah, Wusta and Dhofar 
        Governorate.<br>
        <br>
        In 2000, Siemens was awarded, the first ever IN (Intelligent Network) 
        platform for the establishment in the Sultanate. The first phase of the 
        project was successfully implemented within a short time frame. The 
        public was surprised by the company’s customer, Omantel, with the 
        introduction of Prepaid Cards “Hayyak” &amp; “Jibreen” for mobile and fixed 
        subscribers, respectively.<br>
        <br>
        In 2001, Siemens was entrusted with further expansions of the mobile 
        network in Salalah and rural areas including Dakhiliya, Dahira and 
        Sharqia. In addition, a new MSC/VLR was also established at Nizwa.<br>
        <br>
