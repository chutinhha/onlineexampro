                  <td width="100%"><font face="Verdana" size="4" color="#FF0000">A Camel's Tale</font>

                    <p><font face="Verdana" size="1"><b>How can camels' milk and a dairy farm in the desert save one of Arabia's most unique and treasured environments? OER Editor Matthew Brockett finds out</b></font><font face="Verdana" size="2"><br>

                    <br>

                    <img border="0" src="../../images/2001_july_august/cim1.jpg" align="left" hspace="5" vspace="5" width="225" height="181">
                    When the monsoon descends on the Salalah Mountains each year, soaking them with moisture from June to September, it's hard to believe the lush green environment it creates is part of the Middle East.<br>
                    But for thousands of years the water-laden Salalah Mountains have supported the people of Dhofar in southern Oman. Acting as a water catchment area, the mountains sustain not only the farmers that live on them, but also the people on the plains below.<br>
                    Now, this unique eco-system is under threat.&nbsp;<br>
                    Just as the human population of the area has grown in recent years, so too has the number of animals farmed on the mountainsides. These swelling herds of cattle, camels and goats, which now total almost 400,000, are rapidly destroying the vegetation.<br>
                    "It's the vegetation on the mountains that collects the water from the monsoon cloud," explains David Deeks, project manager at Baraka Economic Consultancy.<br>
                    The monsoon in Salalah (called the Khareef) is not torrents of rain. It is a thick, water-laden cloud that cloaks the mountains.&nbsp;<br>
                    <br>
                    "It needs something -- grass, trees, branches, leaves, whatever -- to catch it," says Deeks. "Water literally can run off trees in rivers. That water then feeds not only the surrounding region, but it feeds into the aquifers, the springs which have for centuries fed the Salalah plains. The Salalah Mountains are very very important to the overall stability of the region."<br>
                    In other words, if the vegetation on the mountains is destroyed, the surrounding region could be deprived of the water it needs to survive.<br>
                    Something needs to be done, but finding a solution has not been easy.

                    </font></p>

                    <p><font face="Verdana" size="2"><img border="0" src="../../images/2001_july_august/cim3.jpg" align="right" hspace="5" vspace="5" width="225" height="192">
                    The people on the Salalah Mountains are different from the people on the Salalah plains. They were once nomadic, moving with the seasons down into the Hadhramaut (the coastal region in the southern Arabian peninsula), further down towards Yemen, and back again.&nbsp;<br>
                    Now there is a border and the people have become more settled, but not without tension.&nbsp;<br>
                    Between 1965 and 1982, fighting marred the Dhofar region. More recently, there have been disputes between the farmers on the mountains and the people on the plains over the price of the animal feed the farmers must buy. In an increasingly open marketplace, they are struggling to compete and have fallen on hard times.<br>
                    "They're quite political, and the concept of cutting back, destroying half the livestock they have at the moment, is something that will not work for them," says Deeks.&nbsp;<br>
                    But Deeks - an agriculturalist who has worked on His Majesty's farms in Salalah - believes he has developed a proposal that will work.<br>
                    "The concept was kind of an overall aid package," he says.&nbsp;<br>
                    "The only solution I could see was to set up something out in the desert with the intention of showing a better way to do things on the mountain."<br>
                    The idea is to establish a dairy farm out in the desert.&nbsp;<br>
                    Using ground water, a herd of 1500 cows will be farmed as a counter-balance to the herds on the mountains.<br>
                    Mother herds will be maintained on the mountains, but some of the young stock will be taken to the farm in the desert so that the herd sizes on the mountains do not increase. Ultimately, the mother herds will be reduced as the farmers are taught how to increase productivity.&nbsp;<br>
                    Meanwhile, the milk produced by the animals on the mountains - cows, camels and goats - will be purchased, processed and sold, increasing returns for the farmers.

                    </font></p>

                    <p><font face="Verdana" size="2"><img border="0" src="../../images/2001_july_august/cim4.jpg" align="left" hspace="5" vspace="5" width="225" height="180">
                    Deeks explains: "The main reason for the 1500 cows in the desert is to balance a milk production schedule. At the moment these farmers don't have an outlet for their milk, because they don't have pasteurisation or modern packaging facilities. We can buy the milk and process it. Our herd will be managed so that it coincides with the seasonal milk production of the herds on the mountains. That way we start to get a production profile which suits the consumption habits in Oman, that is a rise in the summer months and a drop in the winter months."<br>
                    Experienced staff employed to manage the dairy herd would also spend time educating local farmers, impressing upon them the need to take environmental concerns into account.<br>
                    Initially, camels and goats will not be farmed at the facility, but camels' milk will be purchased from the farmers and processed, paving the way for a unique new product on Omani supermarket shelves.<br>
                    "There's nothing wrong with camel milk," says Deeks, "nothing wrong with commercialising it either."<br>
                    He says camels' milk, which is already commercially available in Saudi Arabia, is "slightly fresher and whiter than cows' milk".&nbsp;<br>
                    Eventually, the dairy project will be expanded to include camels so that their herd numbers on the mountains will also be reduced.<br>
                    "The camels in some respects could be worse than the cattle, because they've got a higher reach so they can strip all the greenery from a lot of the trees. They are capable and intelligent enough to literally pull down a tree if they want to. They're pretty smart animals."

                    </font></p>

                    <p><font face="Verdana" size="2"><img border="0" src="../../images/2001_july_august/cim2.jpg" align="right" hspace="5" vspace="5" width="225" height="175">
                    Deeks is confident that the project will succeed because the farmers themselves have come on board. Several years ago, at the behest of His Majesty, they set up a company to represent their interests. That company - Omani National Livestock Development (ONLD) - will take a 20 to 25 per cent interest in the dairy project.<br>
                    The entire project is budgeted to cost RO9.2 million. Of that, RO5 million would need to be provided by the Government by way of a soft loan. Around RO2 million would come from prospective promoters, such as ONLD, and the remainder would be raised though a public share float.<br>
                    According to the feasibility study, the new dairy company would produce 8.9 million litres of pasteurised milk and 2.7 million litres of yoghurt a year.&nbsp;<br>
                    It is projected to achieve turnover of RO4.42 million and a net profit of RO560,000 in its first year of operation, rising to RO5.9 million and RO1.3 million respectively in its fifth year of operation.<br>
                    Deeks says that from generated returns, the company would look to build things like a meat processing plant, so it could sell finished meat product into the market.<br>
                    "Meat processing, a tannery for the leather...it will ultimately grow it into a fairly substantial regional business."

                    </font></p>

                    <p><font face="Verdana" size="2"><img border="0" src="../../images/2001_july_august/cim5.jpg" align="left" hspace="5" vspace="5" width="225" height="181">
                    The Arab Authority for Agriculture Investment and Development (AAAID) has indicated it will support the project if the Omani Government gets involved.<br>
                    "They're specialists in agricultural investment," says Deeks, "and they have already recognised the problems of the mountains. They will invest so long as they find the project ultimately feasible, and it can only really be feasible if the Government is behind us, because we not only need the financial clout of the Government, we ultimately need its political clout."<br>
                    The feasibility reports are before the Ministry of Agriculture awaiting final approval.<br>
                    Deeks and his partners are impatient to get started.<br>
                    "I would hope to see it really moving before the end of the year," he says. "It's important to the people and it's important to the Government to get this situation resolved pretty quickly.&nbsp;<br>
                    "We have to achieve a balance on those mountains."

                    </font></p>

