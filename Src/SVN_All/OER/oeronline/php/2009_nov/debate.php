						<b><font face="Verdana" size="1" color="#FFFCE8">
                        WHAT IN YOUR OPINION IS A GOOD PRICE FOR OIL WHICH 
						STRIKES A BALANCE BETWEEN THE NEEDS OF OILPRODUCING 
						COUNTRIES AND CONSUMERS?</font></b></td>
  </tr>
  <tr>
                      <td height="82" valign="top" width="351"> <p align="justify" style="margin-left: 5; margin-right: 5">
						<font face="Verdana" size="1">At present, a range of 
						$60-80 would most comfortably strike a balance for 
						producers and consumers in the long-term. While oil 
						consistently above $90 will bring dire financial 
						dilemmas in the near-term for consumers, it would also 
						help propel forward alternative fuel initiatives.</font></td>
    <td width="5" height="82" bgcolor="#BCBEC0"></td>
                      <td height="82" valign="top" width="402"> 
						<p style="margin-left: 5; margin-right: 5" align="justify">
						<font face="Verdana" size="1">I think a price of $60-75 
						balances the producer-consumer tension. But what is much 
						more important is price stability. If a $70 average is 
						achieved by running the extremes of $40 and $140 with a 
						downside skew, which has been the pattern of the past 
						three years, this will compound the long term problem.<br>
						There is a structural deficiency in the oil market where 
						you have rising demand, especially from the fast growing 
						and increasingly industrialised emerging markets, 
						challenged by declining non-OPEC production. To offset 
						this, greenfield and marginal production must rise.</font></td>
