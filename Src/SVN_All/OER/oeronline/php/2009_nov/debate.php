						<p class="MsoNormal" dir="LTR" style="text-align: center; text-autospace: none; direction: ltr; unicode-bidi: embed">
                        <b><font face="Verdana" size="1" color="#FFFCE8">
                        &nbsp;OIL TOUCHED A YEARLY HIGH OF $80 PER BARREL 
						RECENTLY – WHAT IMPACT WILL THIS HAVE ON THE GLOBAL 
						ECONOMY WHICH IS RECOVERING FROM A FINANCIAL CRISIS?</font></b></td>
  </tr>
  <tr>
                      <td height="104" valign="top" width="351"> 

<p align="justify"><font face="Verdana" size="1">The global economy will 
continue to deal with $80 oil much as it dealt with $70 oil- adequately. 
Deflation in the short-term<br>
continues to be the greater risk as compared with inflation. Granted, inflation 
and the related and looming dollar weakness will eventually have to be dealt 
with, but oil will need to trade over $90 for a sustained period of time (thus 
the fundamental situation would need to experience a drastic change) to become a 
majorproblem again.</font></p>

						</td>
    <td width="5" height="104" bgcolor="#BCBEC0"></td>
                      <td height="104" valign="top" width="402"> <p align="justify" style="margin-left: 5; margin-right: 5">
						<font face="Verdana" size="1">When oil trades above 
						about $70 for an extended timeframe, inflation pressures 
						compound, as the painful experience of 2007 confirms. In 
						some countries with government subsidies, the tolerance 
						stretches to $85 or so with the pain partially absorbed 
						by government budgets. Nevertheless, at current oil 
						price levels of $75-80, we think core inflation will 
						remain in check for the next year only because of the 
						broader deflationary trends in large sectors of the 
						still-troubled global economy. We expect real GDP, i.e. 
						GDP post inflation, to grow at 2.8 per cent in 2010.</font></td>
  </tr>
