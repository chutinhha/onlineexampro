                        <p class="MsoNormal" dir="ltr" align="center"><b> <font face="Verdana" size="1" color="#FFFCE8">WILL 
                          OIL PRICES TOUCH $100 A BARREL IN 2010? </font></b></td>
  </tr>
  <tr>
                      <td height="96" valign="top" width="351"> <p align="justify" style="margin-left: 5; margin-right: 5">
						<font face="Verdana" size="1">Oil prices certainly can 
						touch $100 a barrel in 2010, but this would be the 
						result of unforeseen circumstances. The global 
						over-supply of both crude oil and finished products 
						(paired with idling refineries and 5 million barrels of 
						Opec spare capacity) will keep a lid on any sharp move 
						higher. Prices will trend higher, but expect range-bound 
						trading similar to the inactivity we recently witnessed 
						around $65-75 for much of 2010. We expect steady buying 
						on dips to continue moving the market higher.</font></td>
    <td width="5" height="96" bgcolor="#BCBEC0"></td>
                      <td height="96" valign="top" width="402"> 

<p align="justify"><font face="Verdana" size="1">Barring any jarring 
geopolitical scenarios, I expect it is unlikely<br>
spot oil price will touch $100 in 2010 although it is likely for the 2011-2012 
timeframe given structural constraints in the oil markets. The recent run-up in 
oil to $80 has been driven both by demand recovery and the dollar’s collapse. 
Since the equity market bottom in early March, the dollar has fallen about 17 
per cent against the EUR and 13 per cent against a diversified currency basket, 
which explains the most recent $10-15 price spike in oil.</font></p>

                      </td>
  </tr>
