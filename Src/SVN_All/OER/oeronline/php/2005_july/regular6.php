<br>
Spotting Trouble<br>
<br>
</font><b><font face="Verdana" size="1">Can investors identify potential 
problems before a company goes bankrupt? Khalid Al-Yahmadi highlights guidelines 
that investors can rely on to identify companies that are headed for financial 
distress</font></b></p>
<p align="center" dir="ltr">
</p>
<p align="justify" dir="ltr"><font face="Verdana" size="3"><b>
<img border="0" src="khlid.jpg" width="100" height="122" align="left" hspace="5" vspace="1">N</b></font><font face="Verdana" size="1">ot 
many people seem to be able to identify the early signs of a troubled company. 
Companies like WorldCom and Enron which once were very strong companies worth 
hundreds of billions of dollars exist no longer. Investors worldwide were in 
denial when they heard that these giant companies collapsed. In fact, their 
collapses came as a surprise even to large shareholders in both the companies.
<br>
<br>
Then the question arises – can investors identify potential problems before a 
company goes bankrupt? Fortunately, the answer is yes. However, it requires some 
hard work involving digging into a company’s financial statements and 
identifying its major activities. In the following section, I will try to 
highlight some general guidelines that investors can rely on to identify 
companies that are headed for financial distress. </font></p>
<ul dir="ltr">
  <li dir="ltr">
  <p align="justify" dir="ltr"><font face="Verdana" size="1"><b>Cash flows </b>
  <br>
  Cash is king! Investors should keep a close eye on cash flow from operation. 
  When a company has negative operating cash flow, it means that it is operating 
  on fresh injections of capital from shareholders or lenders. If this occurs 
  over a sustained period, it's a sign that cash in the bank may become 
  dangerously low and the company can quickly find itself insolvent. <br>
  <br>
  Most investors rely on the net profit figure in the income statement to make 
  their investment decision and ignore cash flow. However, bear in mind that 
  even profitable companies can find themselves in trouble if they have negative 
  cash flow. This can happen, for example, when a rapidly growing business with 
  strong sales makes large investments in inventory, and other assets. At best, 
  there will be a delay between when the company pays out cash for these 
  business costs and when it collects cash from customers. This delay can 
  severely stretch cash flow and drive the company out of business as it fails 
  to meet its obligations to creditors and suppliers. <br>
  <br>
  Investors should stay away from companies that report both profits and 
  negative operating cash flows period after period.<br>
&nbsp;</font></p></li>
  <li dir="ltr">
  <p align="justify" dir="ltr"><font face="Verdana" size="1"><b>High Debt Levels<br>
  </b>Companies with high debt levels are more prone to bankruptcy risk than 
  otherwise comparable companies. Principal and interest repayments place 
  pressure on cash flow, and this pressure is likely to be exacerbated for 
  distressed companies. Because they have a higher risk of default to banks, 
  struggling companies must pay a higher interest rate which in turns hurt their 
  bottom line. <br>
  <br>
  One way to determine if a company is carrying high debt in its books is to 
  look at the total debt-to-equity ratio. Total debt-to-equity (D/E) ratio is a 
  useful measure of distress. It compares a company's combined long and short 
  term debt to shareholders' equity, or book value. High-debt companies have 
  higher D/E ratios than companies with low debt. Normally companies with D/E 
  ratios of above 0.5 are considered as carrying high debt. Enron's 
  debt-to-equity at year-end December 2000 stood at 0.9. At June 2001, it grew 
  to 1.1. Finally, its September 2001 quarterly report showed a D/E ratio of 
  1.4. The company declared bankruptcy in December 2001.<br>
&nbsp;</font></p></li>
  <li dir="ltr">
  <p align="justify" dir="ltr"><font face="Verdana" size="1"><b>Profit declines<br>
  </b>Although profit should be looked at in combination with cash flows, 
  investors should take profit declines very seriously as profit declines or 
  warnings are often followed by a gradual share price decline. Investors should 
  compare quarterly growth rates on profit as well as the annual growth rates. 
  However, quarterly growth rates comparison will not be as relevant when 
  looking at companies that sell seasonal products like beverages. <br>
&nbsp;</font></p></li>
  <li dir="ltr">
  <p align="justify" dir="ltr"><font face="Verdana" size="1"><b>Insider Selling<br>
  </b>As a rule companies listed on the Muscat Securities Market (MSM) are 
  required to report purchases and sales of shares by substantial shareholders 
  and directors. Executives and directors have the most up-to-date information 
  on their company's prospects, so heavy selling by one or both groups can be a 
  sign of trouble ahead. Although, insiders don't always sell simply because 
  they think their shares are about to decline in value, heavy insider selling 
  should alert the investors.<br>
&nbsp;</font></p></li>
  <li dir="ltr">
  <p align="justify" dir="ltr"><font face="Verdana" size="1"><b>Share Price 
  Decline<br>
  </b>Investors should also watch out for unusual share price declines. Almost 
  all corporate collapses are preceded by a sustained share price decline. For 
  example, Enron's share price started falling two years before it went bust.<br>
  <br>
  Investors should learn to distinguish between a price decline signaling 
  trouble ahead and a price decline of fundamentally strong company just because 
  it is currently out of favor. While the former signals disaster, the latter 
  could offer a valuable investment opportunity. <br>
&nbsp;</font></p></li>
  <li dir="ltr">
  <p align="justify" dir="ltr"><font face="Verdana" size="1"><b>Top Management 
  and/or Auditors Resignations<br>
  </b>The sudden departure of key executives or directors can also signal bad 
  news. While these resignations may sometimes be completely innocent, they 
  demand closer inspection. However, when a successful manager or a strong 
  independent director suddenly resigns, investors should take that very, very 
  seriously.<br>
  <br>
  In addition, investors should also be wary of the resignation or replacement 
  of auditors. Naturally, auditors tend to leave a company at the first sign of 
  corporate distress or impropriety. Auditor replacement can also mean a 
  deteriorating relationship between the auditor and the client company, and 
  perhaps more fundamental difficulties within the client's business.<br>
