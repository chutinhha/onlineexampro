<p align="justify" dir="ltr"><font face="Verdana" size="3"><b>A</b></font><font face="Verdana" size="1"> 
great—and lasting—brand is the result of mixing the best product with the best 
people, according to Scott Bedbury, one of the world’s leading brand architects. 
Bedbury, who was responsible for Nike’s Just Do It campaign and the emergence of 
the Starbucks, said: “It takes time to create a great brand, and it is the 
result of the best product, combined with the best people. If these elements are 
right, the company name becomes loaded with intangible qualities, which ensure 
it will become a great brand.”<br>
<br>
Bedbury will be addressing more than 2,000 business leaders at the international 
leadership summit, Leaders in Dubai, November 28-29, where branding and 
marketing issues have been added to the debate agenda after feedback following 
last year’s event. OER is the exclusive media partner from Oman for this 
prestigious global event.<br>
<br>
According to Bedbury, a successful brand is not the result of a clever 
advertising tagline alone. “The words Just Do It were only spoken in one Nike 
advert in eight years; the words themselves didn’t hit home, it was that they 
were born out of a great strategy. Indeed, on paper, those words meant nothing, 
but they started to come to life as part of a corporate culture change.”<br>
<br>
He stresses that top-tier executives should start to be held accountable for how 
they influence the corporate brand. “We are seeing companies hold their top 
executives accountable to brand equity performance, not just financial 
performance. This is particularly important if companies need to streamline: 
there are many ways to increase the financial performance of a business in the 
short-term, but many of these will kill your brand in the long-term.”<br>
<br>
Meanwhile, the increasing dominance of the Internet has forced companies to 
become more transparent and focus more on their core values, according to 
Bedbury. He said: “The Internet has made companies realise they are totally 
transparent – one email from a disgruntled journalist can be made available to 
the world’s media. Companies now have to focus on their core values, as 
customers have access to all sorts of information to help them make a decision – 
from consumer watchdog reports, to competitors’ prices. The Internet has 
levelled the playing field again: it all comes down to the intangible qualities 
of a company – and which brands you trust more.” <br>
<br>
