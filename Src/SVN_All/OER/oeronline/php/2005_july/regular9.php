            <br>
            Bulls on a never-ending fairy tale<br>
            <br>
            </font><font face="Verdana" size="1"><b>Fincorp examines the latest 
            stock movements on the Muscat Securities Market as the index touches 
            an all time high in its history<br>
            </b><br>
            </font><font face="Verdana" size="3"><b>
            <img border="0" src="sto_mar.jpg" width="120" height="119" align="right" hspace="5" vspace="1">T</b></font><font face="Verdana" size="1">he 
            records are meant to be broken. That’s what one can aptly say of the 
            MSM markets during the period under review. A gain of nearly 20 per 
            cent in the 30-days ending June 23, 2005 on the back of a 
            superlative growth during the entire 2005 season speaks volumes on 
            the tremendous buying appetite amongst the investors. This is 
            inspite of the Omantel issue which began earnestly during the period 
            under review. <br>
            <br>
            The highlight of the period has been the Index reaching the highest 
            peak ever in its history, crossing the previous high of 5098 points 
            achieved on February 3, 1998. After crossing the previous peak with 
            ease, the market continued its run to cross the 5500 mark by 
            reaching the pinnacle of 5699 levels before profit taking took place 
            to close the Index on June 23, 2005 to around 5629 points.<br>
            <br>
            The largest market capitalisation stock - BankMuscat - led from the 
            front increasing its value by a huge 32 per cent to close at an all 
            time high of RO11.6. This led to other banking stocks following suit 
            along with the investment and holding companies, resulting in the 
            Banking &amp; Investment sector Index gaining by huge 23.5 per cent. The 
            total value traded was at RO151.5 million in 25 trading sessions, 
            resulting in the average daily traded value rocketing to RO6 
            million, which is the highest during the entire bull phase, 
            beginning from 2001.</font></p>
            <p align="center"><u><font face="Verdana" size="1">MSM Index and 
            Volumes</font></u><br>
            <img border="0" src="sto_mar1.jpg" vspace="5"></p>
            <p align="justify"><font face="Verdana" size="1">National Bank of 
            Oman continued to move further northwards on news of its buyback 
            which got formally approved by the bank’s management during the 
            period under review. National Bank of Oman has tied up with 
            Commercial Bank of Qatar(CBQ) which resulted in the foreign party 
            agreeing in principle to buy a private placement of 10 million 
            shares of RO1 each of not less than RO4.450 per share. Overall, its 
            share price further gained around 420 Baiza to close at R04.8.<br>
            <br>
            Other banking stocks which also sustained the up trend included Bank 
            Dhofar which finally managed to cross the RO5 barrier to close 
            strongly at RO5.310 as the peer mismatch started to dawn on 
            investors. While, OIB continued to go along with the Index with huge 
            volumes to close at RO5.41, gain of nearly RO1.19 during the period 
            under review.<br>
            <br>
            Some of the star performers during the period under review have been 
            holding companies which included Transgulf Inv. Holding, DIDIC, Al 
            Batinah Dev., Muscat National Holding along with Index funds like 
            Muscat Index Fund, The First Mazoon Fund et al.<br>
            <br>
            The volumes also improved further suggesting that there is lot of 
            liquidity in the system and also that the foreign investors could 
            also be eyeing the markets.<br>
            <br>
            Another important development was the successful merger of the Topaz 
            Energy and Marine with Renaissance. The merger is expected to bring 
            synergy in the combined operations and improvement in the 
            shareholders value, which was witnessed in the rising market 
            capitalisation of the combined entity. After TEAM merged with 
            Renaissance and delisted, Al Maha has taken its place in the Index.<br>
            <br>
            The Q1’ 2005 results have also vindicated the moves atleast on the 
            profitability front. The earnings have further improved on quarterly 
            comparison with the last quarter of 2004 results. This was largely 
            driven by good results on a YoY basis by almost all the banks 
            resulting in market investment gains for the holding companies. 
            Overall, these together constitute more than 66 per cent of the 
            Index; the overall earnings went sharply up; almost 20 per cent 
            resulting in the valuations again becoming very attractive.<br>
            <br>
            Interestingly, the announcement of Omantel issue opening also didn’t 
            dampened the sentiment on the market, even though the Index did dip 
            during the initial first week of May, 2005 as the stocks passed 
            through from weaker players (traders) to the stronger or more long 
            term investors. The issue which started on June 11, 2005 is slated 
            to close on July 10, 2005.<br>
            <br>
            The overall market has gained by a phenomenal 66.8 per cent in the 
            first half of 2005 itself ending June 23, 2005 touching a new all 
            time high. Hence, given this abnormal run, doubts have been creeping 
            in the minds of investors whether the existing bull run is 
            reminiscent of the past bull run of 1997-98 period. We believe that 
            the bull run of 1997-98 was different from the current rally because 
            one of the main reasons for the markets to go up during that time 
            was due to the leveraged capital as a result of the margin and trust 
            accounts. This has however changed over the last few years, with 
            margin and trust accounts no longer existing. The only leveraged 
            capital existing in the current market is in the form of bank 
            lending, which is also regulated as per the Central Bank of Oman 
            (CBO) norms. Hence, the current market growth appears to be 
            originated from a real source of investible surplus in the market 
            and not buoyed by leveraged funding.<br>
            <br>
            The last few days of the period under review has shown the classic 
            stepladder formation with the markets consolidating at its newfound 
            levels. If one looks at foreign investments (inc. GCC citizens) over 
            the last few months, than the reason for this is not far to seek. 
            The overall market capitalisation is slowly and surely being taken 
            over by the foreign investors as the valuations between the local 
            and regional markets is still there. <br>
            <br>
            We believe that given the relatively lower valuations and the 
            expectations that the returns from the Omantel issue would not be 
            significant; given the lock-in period for the Omantel issue for GCC 
            investors being kept at around three months, the secondary market 
            could remain buoyant. Hence investors are advised to maintain their 
            holdings for the medium term.</font></p>
