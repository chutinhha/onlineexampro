<p align="justify" dir="ltr">
<font face="Verdana" size="3"><b>
<img border="0" src="legalit.jpg" width="100" height="122" align="left" hspace="5" vspace="1">T</b></font><font face="Verdana" size="1">o 
support their Omani customers, many foreign companies require a more permanent 
presence for their employees. In order for a foreign company to conduct business 
in Oman, it would normally establish a branch or a commercial company in Oman in 
accordance with the Foreign Capital Investment Law promulgated by Sultani Decree 
102/94 (FCIL) and the Commercial Companies Law promulgated by Sultani Decree 
04/74 (CCL). However, some foreign companies have considered circumventing the 
FCIL requirements without realising the potentially dire results. <br>
<br>
<b>Proper Establishment</b><br>
The FCIL provides that non-Omani natural or juristic persons are prohibited from 
carrying out any commercial, industrial or tourism business in Oman without 
first obtaining a foreign capital investment licence. Accordingly, any foreign 
entity that intends to offer any commercial services or products other than by 
way of an agent, or intends to carry out any other business in Oman, must first 
be licensed to do so. <br>
<br>
In order to obtain such a licence, a foreign company will be required to either 
establish a commercial company with a local Omani partner, or to participate in 
an existing Omani company, unless the company has been granted a government 
contract. Due to the limitations on foreign shareholding under the FCIL, most 
foreign entities intending to establish or participate in a business in Oman 
will have to accept some level of Omani shareholding. As noted in my previous 
articles, the minimum capital requirements of companies in which there is some 
level of foreign participation is RO 150,000, unless the CCL requires a greater 
amount, as is the case with joint stock companies, holding companies, or 100% 
foreign-owned companies. <br>
<br>
If a foreign company intends to conduct business in Oman without first having 
obtained a foreign capital investment licence or other necessary approvals, it 
will be in violation of the law and will be subject to fines and penalties. <br>
<br>
Article 17 of the FCIL provides that any foreign entity that conducts any 
business in Oman without first obtaining the required foreign capital investment 
licence shall be punished by a fine of not less than RO5,000 and not more than 
RO10,000 Furthermore, any Omani who participates with the foreign entity in 
conducting business in Oman in violation of the FCIL shall also be subject to 
fines.<br>
<br>
<b>Expatriate Businessmen</b><br>
But some foreign companies have been hesitant to set up permanent establishments 
according to FCIL and CCL requirements. Instead, they have considered the 
possibility of making arrangements with local companies by which such local 
companies would sponsor the foreign company's expatriate employees in order that 
those employees may provide goods and services to their customers in Oman. 
Although this may sound clever, it is actually illegal.<br>
<br>
The Law of Expatriate Residence promulgated by Sultani Decree 16/95 (&quot;Expatriate 
Law&quot;) defines an expatriate as any person who does not hold Omani national 
status. According to Article 10(3)(b) of the implementing regulations of the 
Expatriate Law, a foreign person entering Oman under a visa issued for a 
commercial assignment may only engage in commercial negotiations or the 
promotion of commercial relations between the sponsor company and the foreign 
company. Any attempt to provide goods or services in Oman to customers of the 
foreign company would go beyond the intended scope of the visa and necessitate 
the acquisition of a foreign capital investment licence by the foreign company. 
Furthermore, expatriate employees entering Oman under such visas are sponsored 
by local companies. In the event the expatriate employee conducts business for 
its foreign employer, the local sponsor will also be subject to fines and 
penalties.<br>
<br>
Additionally, Article 15 of the Expatriate Law provides that any expatriate who 
wants to reside in the Sultanate of Oman for a contiguous period of more than 3 
months must obtain a residence permit. According to the Expatriate Law, it is 
only legal to grant residency permits to those expatriates who are intending to 
reside in the Sultanate for employment purposes. <br>
<br>
Article 17 of the Expatriate Law further states that a residency permit shall 
only be granted to expatriates who enter Oman through the sponsorship of a local 
employer. However, some foreign companies have considered approaching local 
sponsors in order to secure residency visas for their expatriate employees in 
order that they may conduct business in Oman without an FCIL licence. Again, 
this is illegal. <br>
<br>
Finally, the Labour Law of Oman promulgated by Sultani Decree 35/2003 (the 
&quot;Labour Law&quot;) specifically provides in Article 18 that non-Omani employees shall 
not be permitted to work in Oman unless they have obtained a labour card. <br>
<br>
According to the Labour Law, a labour card will only be issued if the non-Omani 
has an employment contract with an Omani employer, or with a non-Omani employer 
provided that such employer has obtained the requisite clearance from the 
Ministry of Commerce and Industry (&quot;MCI&quot;). Therefore, any attempts by a foreign 
company to obtain labour clearances for its expatriate employees through the 
sponsorship of a local company will also be in violation of the law.<br>
<br>
<b>Penalties</b><br>
Article 41 of the Expatriate Law provides that anyone providing false 
information or submitting documentation that contains factual errors with the 
intention of obtaining any type of residency permit shall be punished by 
imprisonment for a period of not less than one month and not more than 3 years, 
and with a fine of not less than RO100 and not more than RO500. Both the local 
sponsor and the employee may be subject to the fines and imprisonment as well.<br>
<br>
Article 114 of the Labour Law provides that whoever employs a non-Omani employee 
without a proper clearance authorising him to do so shall be punished with a 
fine of not less than RO10 and not exceeding RO100. The penalty shall be 
multiplied by the number of workers who are the subject-matter of the 
violations. Moreover the employer shall be bound to pay the expenses of 
returning the worker to his country and shall be deprived of bringing expatriate 
employees into the Sultanate for a period not exceeding one year. <br>
<br>
Article 114 also provides that an expatriate employee who works in the Sultanate 
without a proper clearance from the competent department, or who works with an 
employer who has not been authorised to bring him into the Sultanate, shall be 
punished with imprisonment for a term not exceeding one month and/or with a fine 
not exceeding RO100. Moreover, the clearance issued, if any, shall be cancelled.<br>
<br>
<b>Play it safe</b><br>
Considering the substantial penalties associated with these laws, it is 
advisable for foreign companies intending to offer goods and services to 
lawfully establish a permanent presence in Oman and directly sponsor their 
expatriate employees, rather than trying to find ways around the residency and 
labour laws of Oman. <br>
