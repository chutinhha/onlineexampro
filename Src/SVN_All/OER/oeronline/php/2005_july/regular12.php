Working Out of Bounds<br>
<br>
</font><b><font face="Verdana" size="1">If the recent launch of the $15 billion 
Al Madina al Zarqa complex (AKA Blue City) is any indication, Oman's growth as a 
commercial and tourism hub is moving steadily forward, and Omani and 
