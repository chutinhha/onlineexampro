<font face="Verdana" size="1">sure realise that I ended my previous article with a promise to give some good 
examples of the efforts made by some interesting brands, to address the five 
senses of the human. I know someone who read my previous articles and asked me 
if I could highlight certain good examples of brands that have done some good 
work in sensory branding and positioned themselves well ahead of the bandwagon. 
I refrained from doing so in the past; since taking the names of brands would 
mean curioisity from the brand owners to know what that was all about. In any 
case, the idea seemed good and definitely would add value to people who observe 
why some brands are more successful than the rest and what they did differently. 
I did have various opportunities to see and gather more, which8 I shall try and 
communicate through my articles sooner or later. <br>
<br>
Before I get on with keeping my promise about a few good examples, I need to 
explain an interesting expression “Jinba Ittai”, which I chose to be the title 
to my article this month. From the sound of it you would know it is Japanese and 
certainly something that creates excitement. It is indeed Japanese and the 
direct translation of this idiom is “Rider and horse as one”. An interesting way 
of expressing something so intense. “Yabusame” a long standing artistic ritual 
ceremony in Japan, truly embodies the essence of Jinba Ittai. An archer mounted 
on a horse back gallops past his target and shoots an arrow. To achieve a 
bull’s-eye, the archer and the horse must move as one. There must be a natural 
two-way communication and a high level of synergy in their alliance. This 
oneness of motion between the rider and a horse was selected as the most apt 
analogy depicting the relationship between the driver and an exhilerating sports 
roadster manufactured by the exciting and youthful Japanese brand Mazda. <br>
<br>
The rider and the horse idiom and the effort to create a car, universally seen 
as “lots of fun”, served as the focal point around which the original and the 
all new Mazda MX – 5 were designed and engineered. While most sports cars aim 
for specific performance targets only, such as time required to accelerate to 
100 kmph or the cornering G provided by the chassis, the Mazda engineers 
established additional goals to reinvigorate this light weight sports car. In 
essence, this became a celebration of the simple delights of driving an open 
roadster. The “Fun” was designed for anyone and any location during sports 
driving and daily life. Human emotions do count. Why this expression is relevant 
in this article is for you to decipher and I am sure you will realise that some 
clarity on the “Human feel factors” will give you a bit more insight into it. 
Weren’t we talking sensory branding? I know I have chosen the one brand that 
will always figure at the top and will remain among my favorites here; to 
express this, but then don’t we all say things and take names of those people or 
things close to our heart? Just one of those human tendencies I guess. Something 
that most big brands around are trying their best to work with. I am indeed 
trying to lead into the ever so important sensory aspect to the branding 
principle here, when I talk about even a minor effort here.<br>
<br>
While we know that the effects of branding campaigns are on a steady decline, as 
the costs of reaching out to consumers in an ever busier world is on the rise; 
we see opportunity if we were to get specific about who we are and address 
specific sensory elements of humans. Despite the decline in effectiveness, we 
all know that advertising is here to stay. I would argue that the way we 
communicate the messages needs to be re-evaluated to be more closely aligned 
with todays world. Something new is needed to break the two dimensional 
advertising impasse. Superficial attempts will not do it. We have seen 
incremental improvements have been made in some areas such as, creating 
advertisements that mirror the theme of a programme, and this to some extent, 
help to keep viewers watching. While various attempts are being made in the 
advertising industry to capture the essence, advertising still remains a mere 
flash in the daily life of a consumer. Maybe there is an opportunity, if we 
broadened our horizons to encompass as many human senses as possible in our 
messages. <br>
<br>
Human senses and the tendencies to do certain things in a particular way can be 
observed and addressed. Martin Lindstrom, someone I respect for having given us 
this insight, made a good attempt to highlight certain integral elements of 
humans and how they can be influenced. For instance, we sniff the milk that we 
take from the refrigerator before drinking it; we also sniff at the slightest 
indication of smoke and then act accordingly. Our sense of smell keeps us safe 
and we trust that little, but important, element of ourselves much more than 
what we realise. Over the years, the advertising world has indulged and catered 
to another important sense of ours that we trust so much, which is the sense of 
sight and they have ensured optimal visual satisfaction. Researchers have proven 
the fact that our senses, besides the sight, such as smell, sound, touch and 
taste give us almost our entire understanding of the world. Events, moods, 
feelings and even products in our lives are continuously imprinted on our five 
track sensory recorder from the second we wake up to the moment we sleep. <br>
<br>
This, despite the fact that most communication including advertisements that we 
are exposed to on a daily basis comes to us only on two of the five available 
tracks. If you think for a moment, we know that it is only the visual and the 
sound sense that we try to explore. They say human beings are most effective and 
receptive when operating on all five tracks or when all the five senses are 
actively touched upon. Yet most advertisements do not address this aspect of the 
human being.<br>
<br>
Let us see if we can remember the first new car that we bought. For everyone the 
first new car always leaves a major impression in the mind. One distinct thing 
we remember is the brand and also that it had a definite new car smell. Many 
customers I see, cite the new car smell as being one of the most gratifying 
aspects of purchasing a new car. The smell is as much a statement as the shiny 
body and exceptional features that you see. As I understand, the fact remains 
that there is no such thing as a new car smell. It is an artificial construct, a 
very highly successful marketing ploy that taps directly into fantasy. This 
smell can be found in the aerosol cannisters on the factory floor, that contain 
the new car aroma. As the car leaves the production line, the scent is sprayed 
throughout its interior, but the life of this scent is not very long if not kept 
under wraps. <br>
<br>
Once we buy our car then the smell of everything else overtakes and no feature 
in our car will tell us then that our car is no more new. However, we can 
prolong that “new car” sense by buying another aerosol cannister from an 
accessory shop and experience the same new car feel. Do you see how we are so 
unaware of the way our senses interact with our day to day experience?<br>
<br>
Most big brands that does things diffrently to continue to stay on top, address 
the power of suggestion well, by touching the different senses of a human being 
and luring their thoughts into a positive one for them. Let us look at what some 
biggies in the different industries (I shall refrain from only talking 
automobile brands) did and how they have managed to stay on the top layer. I am 
not particularly talking about the automotive industry here since we see some 
real good examples outside the industry as well. As for cornflakes, Kellog’s 
considers the crunchiness of the grain as having everything to do with the 
success of their breakfast cereal. Emphasis is placed on the crunch we hear and 
feel in our mouths, rather than the sound effects we hear on commercials. <br>
<br>
They spent many good years experimenting with the synergy between crunch and 
taste and it is said that as part of their research they made contact with a 
Danish commercial music laboratory that specialises in the exact crunchy 
sensation of a breakfast cereal. Since Kellog’s wanted to patent their own 
crunch, and trademark and own it in the same way they own their recipe and logo, 
they got the laboratory to create a highly distinctive crunch, very uniquely 
designed for Kellog’s. Anyone who has helped themselves to some cornflakes from 
an unmarked glass bowl at a breakfast buffet, would recognise those anonymous 
cornflakes as Kellog’s. A great example of a brand addressing the human senses 
well. The day Kellog’s introduced their unique crunch to the market, their brand 
moved up the ladder. They expanded the perception of their brand to incorporate 
four senses, including touch rather than the more limited sight and taste. They 
broadened their platform by appealing to yet another of our five senses.<br>
<br>
Another interesting brand would be the Singapore Airlines. Earlier on, airlines 
based their promotions on cabin design, food, comfort and pricing; ignoring the 
total sensory experience they could offer. In 1973, the Singapore Airlines broke 
through the barriers of traditional branding with their Singapore girl, a move 
that would prove so successful that in 1994, the Singapore girl celebrated her 
twenty-first birthday and became the first brand figure to be displayed at the 
famous Madame Tussaud’s Museum in London. Singapore Airlines made a shift when 
they introduced a campaign based exclusively on the emotional experience of air 
travel. Just to add to this, Singapore Airlines addressed another dimension of 
the senses by introducing Stefan Floridian Waters, which is an aroma that has 
been specifically designed as part of the Singapore Airlines experience. This 
perfume formed the scent in the flight attendant’s perfume, was blended into the 
hot towels served before take off and generally permeated the entire fleet of 
the airlines planes. This patented aroma, very distinctly felt and longed for by 
passengers, has since become a unique trademark of Singapore Airlines.<br>
<br>
The Coca Cola brand, when examined visually, has a very clear sense of colour. 
The red and white colours stands out where ever Coke is seen. Did you know that 
Santa Claus traditionally wore green until Coca Cola began to promote him 
heavily in the 1950. Now, every where Santa wears the colours of Coke.<br>
<br>
Bang and Olufsen appealed to the tactile sense with their all in one remote 
control. This invention, which first appeared in 1985, has evolved to become a 
streamlined sensual piece of equipment that oozes quality. Many companies 
introduced similar equipment but the Bang and Olufsen remote is heavy, solid and 
quite distinct. This sense of Gravitas; which makes consumers link to quality, 
is duplicated across every Bang and Olufsen product line. <br>
<br>
From the automobile industry, one of the greatest examples of the sensory 
branding has been the Eau de Rolls- Royce. Millions of dollars have been spent 
in reproducing the distinct smell of the 1965 Silver Cloud Rolls-Royce. The 
smell is impossible to buy, yet it has been an essential component in 
maintaining one of the world’s primary luxury brand. When Rolls-Royce started 
getting complaints about their new models not quite living up to their 
illustrious predecessors, they worked out that the only difference between the 
new and the old, apart from the obvious, was the smell. <br>
<br>
The interiors of the older Rolls smelled of natural substances like wood, 
leather, hessian and wool. Modern safety regulations and building techniques 
mean that most of these materials are no longer used and have been replaced by 
foams and plastics. The only way to recapture that essence was to artificially 
mimic it. Using a 1965 Silver cloud as a reference, the team did a detailed 
analysis of its aroma and identified individual odours. Surprisingly about eight 
hundred different elements were found and besides some like mahogany and 
leather, which was expected, smell of oil, petrol, underseal etc. were a real 
shocker. The need for this distinction, ensured that this distinct smell had to 
be recreated artificially and that was done. Now, before each Rolls-Royce leaves 
the factory, the unique smell of the Rolls- Royce is added to the under sides of 
the car’s seats to recreate the smell of classic Rolls. This attempt turned out 
to be a real winner.<br>
<br>
Did you know that carrots once came in every colour but orange. Sometime in the 
sixteeenth century, the Dutch farmers decided to give this root vegetable a 
patriotic edge. It is understood that using a mutant seed from North Africa, 
breeders began developing an orange variety in honour of their monarch, William 
I, the Prince of Orange, who led them to independence against the Spaniards. A 
country with an orange coloured flag had now created an orange carrot. Very few 
people who munch on a carrot now are aware that they are biting into one of the 
biggest marketing blunders and the greatest missed branding opportunity . <br>
