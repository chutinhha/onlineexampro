<b><img border="0" src="telecome1.jpg" width="190" height="251" align="left">O</b></font><font face="Verdana" size="1">man 
has been trying, seemingly for an eternity, to privatise its telecom sector. The 
opening up of the mobile sector to the second operator in the private sector was 
the first stepping-stone. The second, listing part of its monopoly fixed line 
services provider on the Muscat Securities Market, seemed elusive. So was a 
possible technology-equity partner for the state-owned company. Not even the WTO 
commitments seemed to be able to force the pace; the Telecom Ministry did 
restructure the Oman Telecommunications Company but then came 9/11 and plans 
went awry. <br>
<br>
Finally came June 11 ushering into the Oman telecom sector a new era with the 
Omantel IPO opening on the MSM. Oman had kept its word with the WTO and the 
Sultanate had kept its promise to its people: to provide opportunities to 
participate in wealth creation and nation building. <br>
<br>
From the buzz around Muscat, the feeling is that the Omani is organising his 
funds to put his money where his mouth is: into the instrument that helps him 
talk, talk and talk. Only this time, it is stock talk; at the Muscat Securities 
Market; and the talk is around telecommunications: a sector that offers enormous 
growth opportunities in a country emerging from its technological nascence and 
fast catching up with global standards. <br>
<br>
At the bottom of the technology ladder is the philosophy of making phones – 
fixed or mobile – accessible by all. At the top end is state-of-the-art 
connectivity for exploitation by sophisticated telecom and IT applications that 
will strategically launch Oman into the realms of a knowledge society. The 
instrument of this change is primarily Omantel, the state-owned telecom company, 
with a RO250 million investment-spend lined up. It has recently been exposed to 
the winds of competition on the mobile circuit and is in the market to offload 
30 per cent of its equity exclusively to the Omani individual. The key here is 
‘exclusively for the Omani individual’, as Omantel seeks to raise a hitherto 
unattempted RO300 million in the Sultanate to set a precedent in the Gulf region 
