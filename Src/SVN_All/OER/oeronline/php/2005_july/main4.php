There are 1.2 million urban Omanis and 500,000 rural Omanis (less than 40 per 
cent under 15) for the purposes of establishing a credible IPO target. Then 
again, the local currency deposits in Oman are held in some 1.02 million 
accounts with an average balance of around RO 2,350. <br>
