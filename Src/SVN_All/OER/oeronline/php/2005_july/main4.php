to the IPO news; falling from a 52-week high of 4674.61 points (April 28, 2005) 
pursuant to the Omantel IPO story, then registering a 43.5 per cent drop in 
turnover in April 2005, to RO86.53 million with aggregate market capitalization 
in end April at RO4.37 billion compared with RO4.25 billion. Such moves were 
associated with people organising funds for the Omantel investment. 
Nevertheless, markets perked up as the IPO drew near and three days before the 
opening (June 8), the intra-day index breached the 5,200 mark.<br>
