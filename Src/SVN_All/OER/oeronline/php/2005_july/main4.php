<p align="justify"><font face="Verdana" size="1">
There are two aspects to this massive telecom IPO that is generally in keeping 
with regional trends of using such IPOs as vehicles to privatise. 
Notwithstanding its umbilical links with the government and that telecom 
represents an unquestioned growth area in Oman – which add to the IPO’s muscle – 
there is the Omani mindset and the current telecom infrastructure to contend 
with before one celebrates success. Both are reflected in the telephony usage in 
the country. There is also the mindset around investment in equity, which is 
dictated by sociological and not economical considerations and where the 
‘exclusively for Omani’ factor may severely limit the audience for the IPO, 
never mind the state of the stock market, the quality of the offering, the 
solidity of the corporate and the growth potential of the industry. More of that 
later, though. <br>
<br>
In terms of numbers, from 15,000 post-paid mobile subscribers in 1996 to 257,287 
in 2004 is a satisfying scenario in a country of around 3,001,583 people even 
though fixed line growth has been a tad too slow. Experts place Oman’s current 
mobile market potential at 1.6 million subscribers, which mean an enormous 
growth vista. Oman also has a pre-paid GSM base of around 548,993 (2004) and 
this cellular penetration rate is projected to cover 50 per cent of the 
population by 2008, with more than 1.5 million subscribers. From a purely 
marketing perspective, this represents an overwhelming familiarity with the 
name, Omantel.<br>
<br>
The Oman telecom story is not about numbers though. It is one around quality of 
technology, its reach and efficiency that Oman is trying to put in place. Thus, 
at one level is ‘project oxygen’, waiting to revolutionise the international 
telecommunications services through value-added services running on very high 
bandwidths and application of such technologies as Asynchronous Transfer Mode 
(ATM) for the delivery of voice, data, and video (multimedia); at another plane 
is the Asymmetric Digital Subscriber Line (ADSL), the high-speed broadband 
internet-service; or the super fast Internet service based on a broadband 
Digital Subscriber Line (xDSL) network infrastructure.<br>
<br>
Yet again, there is the Omantel tie up with Flag Telecom that positions the 
Sultanate as a regional hub in the advanced broadband communication services 
being instituted by the submarine cable system company, Falcon, that is poised 
to unlock the broadband opportunity along the entire route and provide the first 
direct terabits link between two emerging global giants, India and China, with 
the Gulf linkages, of course. <br>
<br>
If all this is not enough, there are initiatives such as the Knowledge Oasis 
Muscat, in the public-private sector domain that promises to create an enabling 
platform for companies, entrepreneurs or researchers of every hue and size to 
launch and take their businesses forward. With multimedia messaging service 
available, corporate and private subscribers can exchange multi-media content 
across Omantel and global networks. <br>
<br>
Thus a tradition-bound Oman, the oldest independent state in the Arab world, 
which had chosen to remain more isolated than some of its more flamboyant 
neighbours, is on a technology mission, taking off from the early foundations 
laid by the Arabsat regional earth station in Al Amerat in 1985, which provided 
Oman the primary means for international telecommunications via satellite. Oman, 
it may not be known, was among the first countries to join Arabsat. <br>
<br>
More than a decade down the line came the Internet in January 1997 with a 60 per 
cent annual growth and a current customer base in excess of 49,866. For the 
Internet, it has been a technology trail from the dial-up service of 56.6kbps to 
the IDSN of 128kbps and the ‘powernet’ (ADSL) for Internet access at speeds up 
to 512kbps. For corporate users it has the MaxNet. Today, the telecom sector 
finds to its delight that the wider its services get, the higher grows the 
demand for more services, to be used by more people, as convergence of 
technologies make for new opportunities. Among the most exciting options for the 
Omani youth is the gaming option that has the potential of becoming big business 
in Oman and driving demand for bandwidths.<br>
<br>
<img border="1" src="telecome2.jpg" width="150" height="175" align="right" vspace="1">Guiding 
the telecom movement is the National Committee for Information Technology that 
has strategised on the basis of the need to establish a “sophisticated and 
secured telecommunications infrastructure, building capabilities to make use of 
the information society through complete integration of IT and communications 
with learning and training are the main aspects”, according to Eng. Mohammed bin 
Ali Al Wahaibi, the Omantel Executive President. The strategy also includes 
creating awareness by highlighting the importance of IT and communications for 
the knowledge-based economy and its role in the information industries.<br>
<br>
Surely this is the dream scenario for any industry with the added potential 
contained in Oman’s moves to address the fact that it still remains one of the 
least-connected countries in the Gulf Cooperation Council. The commitments were 
crystallised at the time of Oman’s entry into the World Trade Organisation: Oman 
would privatise this sector both in terms of ushering in competition – without 
which it could not expect rapid technological advances – and partly 
relinquishing State ownership. <br>
<br>
His Excellency Shaikh Mohammed Bin Abdullah Al Harthy, Oman’s Minister of 
Transport and Communications, made two significant statements earlier this year 
when he described Oman's economic direction to be in line with the “contemporary 
trends of trade and investment liberalisation, which are adopted by the WTO, of 
which the Sultanate is a full-fledged member” and confirmed that the Ministry of 
Finance would offer a part of Omantel equity in the form of an IPO, “which will 
definitely add to the activity of the MSM”. <br>
<br>
Thus, talk of the Omantel IPO has been around for a while and if it had been the 
objective of the government to test the waters without even touching it, it had 
succeeded. The IPO talk has been whetting investor appetite and the current 
summer was predictably the right season for the plunge. The caution was perhaps 
well advised because the privatisation plan had to keep the Omani national’s 
interest in mind even as Oman wanted to ensure adherence to international 
commitment. That gave birth to the line of thinking that the Omantel offer would 
be for the Omani individual only. Caution was also advised because of the 
relative infancy of the Omani telecom developments even when compared to other 
Gulf countries. In fact, the Sultanate made a concerted bid for a liberal 
telecom regime only in 2003, by seeking out “leading-edge technology, attracting 
multinational telecom operators, service providers and manufacturers”, as HE Al 
Harthy concedes. <br>
<br>
Year 2003 also saw the establishment of the Telecom Regulatory Authority to 
oversee the process of establishing the competitive framework for telecom 
services: Omantel's exclusive rights to provide services was whittled down and 
within a couple of years, Nawras Telecom was up and running with its second 
public mobile licence. Yet, there are growth issues around the Omani telecom 
sector. Though the Omani is fond of talking over the phone, the fact is that 
fixed line penetration is a poor 9.2 per cent though mobile penetration is a 
happier 24.2 per cent. <br>
<br>
At the end of December 2004, there were 228,323 fixed subscribers plus 6,530 pay 
phones. These numbers are projected to increase to 290,000 by end 2008. Possibly 
by then, Oman will have another fixed operator in terms of current plans that 
suggest competition within a couple of years. This is a relatively swift 
denouement of the privatisation movement that began with the legal and 
organisational establishment of Omantel in mid-1999, replacing the State-owned 
monopoly, the General Telecommunications Organisation, with the Minister now 
publicly talking of plans to liberalise the fixed line market.<br>
<br>
<img border="0" src="telecome3.jpg" width="200" height="145" align="left" hspace="5">The 
speeding up of the privatisation effort has seen the current operator being 
“split into two separate companies under the control of a holding company (Omantel). 
The separate mobile entity (Oman Mobile) was created in February 2004. Omantel 
is further expected to split, giving rise to an independent Internet service 
company, on the lines of Oman Mobile”, the minister has said. The competition 
between the fixed telecom suppliers is expected to improve the country’s fixed 
line network and penetration figures, he believes. <br>
<br>
Meanwhile, the fixed-mobile substitution is on an accelerator even as the 
average fixed lines growth is projected at no more than 4-5 per cent over the 
next five years. Worse, 47 per cent of the Sultanate's fixed lines are in 
Muscat, the capital, a much higher penetration rate of 17 per cent against the 
rest of Oman’s 7 per cent. Overall, Oman’s fixed line capacity is placed at 
270,000 connections, with a penetration of around 14 per cent.<br>
<br>
Nevertheless, Omantel has been quite a goose for laying golden eggs with its 
comprehensive services featuring fixed, mobile, data and Internet facilities, 
reporting a net profit of RO72.8 million in 2004 and a projected RO74.6 million 
for year 2005. Omantel that increased its capital to RO75 million from RO50 
million has had a comfortable ride on the enhanced subscription to its GSM 
services. <br>
<br>
However, the net profit declined from RO104.7 million in 2003 to RO72.8 million 
in 2004. This decline is principally due to the royalty fees of RO31.6 million 
in 2004. It must be recalled that with effect from 2004, the Omantel Group is 
required to pay annual royalty fees at the level of 10 per cent of gross 
revenues under the Fixed License and 12 per cent of gross revenues under the 
Mobile License which is putting significant pressure on the profit margins for 
Omantel. But service revenues are increasing steadily. Omantel generated service 
revenues of RO244.9 million in 2004 on the base of RO211.1 million in 2003 and 
RO192.8 million in 2002. <br>
<br>
Even a casual appraisal of the Oman market would make the possible synergies 
emerging out of the new found competition obvious. Considered purely from the 
IPO perspective, Omantel has been well served by all the telecom talk associated 
with the Nawras launch. It provided an opportunity for a comprehensive 
assessment of the potential of this sector and highlighted the fact that 
customer service, competition and lowering of costs would drive growth and that 
Omantel would have to achieve the essence of its slogan ‘Together, Anytime, 
Anywhere’, through “Strengthening the relation between the company and its 
customers”, to go by Omantel’s, GM - Customer Services, Abdulhaleem Ali Al 
Sabbagh. <br>
<br>
This was being driven through 32 centres across the country with cost to 
customer addressed on a priority basis. For starters, international tariff was 
revised downwards for as many as 140 countries by as much as 33 per cent and, 
while peak and off peak rates remained the same, the pulse rate was revised to a 
30 seconds pulse for fixed, GSM and Hayyak services. <br>
<br>
Alongside came the automated bill payment machines from mid-March 2005, which 
are being expanded in terms of coverage and customer convenience. Keeping the 
customer in focus has also meant introducing such other payment channels as OIFC, 
OAB and the Oman National Electric Company. Another feather was added to the 
Omantel cap in March this year in the shape of a RO350,000 spend on a new voice 
messaging service to increase the call completion rates. The system which took 
four months to commission provides GSM subscribers with a basic voice messaging 
service free of connection and monthly charges. <br>
<br>
There was equal focus on providing services in remote and urban areas by fixing 
public phones through the V-SAT network and by providing service to the small 
residential groups through the WLL network. A RO3.4 million programme is being 
executed by the Huawei from China to hook up the country, some 200 villages, 
including the remotest, to its fixed phone network. WLL uses mobile technology 
for land telephone service and is considered to be suitable for Oman in view of 
the distances of the interiors from the capital and the mountainous terrain and 
the cost efficiency of the system. Hopefully, all un-serviced remote and rural 
areas will be covered over the next phase at an estimated spend of RO8 million.<br>
<br>
Keeping the customer – individual and corporate – as its core focus, the telecom 
sector is trying to achieve three critical missions: it is getting set to lay 
the tracks on which the government can launch its comprehensive e-governance 
programme; it is seeking to achieve the frontiers in telecom technology; it is 
seeking to lay the foundation of Oman’s emergence as a knowledge society. <br>
<br>
For Minister, Al Harthy, “allowing and providing e-governance service is an 
integral component of the Sultanate’s IT strategy. Oman’s plan of e-governance 
will get off to a major start with the move to launch government convergence 
network by the Ministry of National Economy this year”. E-governance in Oman is 
being considered as the means to improve service “to business and citizens, 
promoting economic and social development and improving the effectiveness and 
efficiency of internal government operations”. <br>
<br>
This brings one to the prospects of the IPO and what it offers to the Omani 
national. It does offer an investment window into a technology sector that 
enjoys infinite opportunities in Oman. The question is not around the quality of 
the offer as one has appreciated; it is around the mindset of the investor: At 
best, Oman has around 30,000 prospective investors of whom the high net worth 
segment would optimistically number around 15,000. The onshore liquid wealth 
owned by affluent Omani individuals was US$2.9 billion in 2003 and high net 
worth liquid wealth accounted for 57.4 per cent of total affluent onshore liquid 
wealth. The Omantel IPO, however, is not about the rich Omani. On principle, it 
is about encouraging the equity cult for the ordinary Omani, through creation of 
worthwhile investment opportunities. While, there can be no official talk about 
it, dividends from Omantel should be there for the asking. <br>
<br>
From minds to the (equity) market is a fairly long distance to travel in Oman 
and focus though the Omantel IPO might be on the ordinary Omani to apply for 
equity, it may have to bank heavily on the wealthy Omani. A perusal of the 
regional telecom experience reveals that while IPOs have been the tried and 
tested avenue to raising telecom funds through privatization, nothing has ever 
been tried at this scale (see chart) with individuals only. The others have been 
open to retail and institutional investors, both domestic and overseas and have 
been well received and expatriate investments have been instrumental in some 
very satisfying over subscriptions. <br>
