    <p style="margin: 5px"><b><font face="Verdana" size="1">Sohar Refinery 
    ‘critical’ </font></b></td>
  </tr>
  <tr>
    <td>
    <p align="justify" style="margin: 5px"><font face="Verdana" size="1">
    Development of the $1.2 billion Sohar Refinery project has reached a 
    critical phase with the loading of catalysts in the plant’s Residual Fluid 
    Catalytic Cracking Unit (RFCCU). Fluid catalytic cracking unit catalyst 
    regenerator means one or more regenerators (multiple regenerators) which 
    comprise that portion of the fluid catalytic cracking unit in which coke 
    burn-off and catalyst or contact material regeneration occurs, and includes 
    the regenerator combustion air blower(s). Residual Fluid Catalytic Cracking 
    Unit Cracking Unit handles the residual fuels. Yacoub Yousif Bilal, CEO of 
    Sohar Refinery Company (SRC), described the catalyst loading as a “major 
    milestone”, representing the starting point of the operational phase of 
    commissioning activities at the plant. Bilal said this while making the 
    formal announcement of the award of a contract for the supply of RFCC 
    catalysts to Engelhard Corporation of the US. Engelhard has been contracted 
    to meet SRC’s requirement of catalysts and additives necessary for the 
    cracking of residual fluids. The refinery’s daily consumption of RFCC 
    catalysts is estimated at around 20 tonnes per day. The catalyst, bagged in 
    1-tonne bags and containerised in 40-foot containers, is shipped from 
    Engelhard’s plant at Savannah, Georgia to Oman via Jebel Ali. With the SRC 
    contract, Engelhard now controls around 40 per cent of the worldwide RFCC 
    catalyst market.</font></td>
