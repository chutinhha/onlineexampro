  </tr>
  <tr>
    <td>
    <p align="justify" style="margin: 5px"><font face="Verdana" size="1">
    Utilization of gas is on the upswing across the Sultanate. Recently, Raysut 
    industrial estate witnessed the inauguration of the gas supply network. The 
    4-km long network, costing $1.5 million, processes and transports 17,030 
    cubic metre of gas per day to supply to 30-50 small and medium factories. 
    The prestigious project was completed in nine months. Sultan Al Habsi, 
    Executive President of the Public Establish for Industrial Estates, pointed 
    out that the network included projects to supply to small and medium 
    establishments. So far, three projects have benefited through this gas 
    supply line. Oman Oil Gas Company is already servicing industrial estates at 
    Rusayl, Sohar and Nizwa.</font></td>
