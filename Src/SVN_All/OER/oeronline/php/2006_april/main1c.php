  </tr>
  <tr>
    <td>
    <p align="justify" style="margin: 5px"><font face="Verdana" size="1">
    Hypermarkets may have arrived and global products may have come to occupy 
    larger shelf space in the Sultanate of Oman. Yet, interest in home-made 
    products has not waned. Sohar witnessed a throng of curious onlookers at the 
    recently held Omani products exhibition where 49 manufacturing companies 
    participated. Units from Buraimi and Rusail have taken the initiative and 
    challenge of displaying their wares — foods, juices, textile, leather, 
    paper, plastic, chemicals, non-mineral raw material industries, metal 
    industries, and equipment and tools.</font></td>
