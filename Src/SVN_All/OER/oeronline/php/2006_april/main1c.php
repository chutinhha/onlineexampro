    Services Company (SWSC) drew a large crowd. With the Deputy Governor Sheikh 
    Abdullah bin Saif Al Mahrouqi presiding over the function, Director General 
    of SWSC Ghali bin Ahmed Al Mashali drew the attention of the gathering to 
    the new emblem, which, according to him, reflects the importance of water 
    and the environment through treatment of wastewater for various uses that 
