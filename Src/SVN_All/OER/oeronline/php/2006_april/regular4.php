<p align="justify"><font face="Verdana" size="1"><b>Land Rover’s hybrid drive 
technology</b><br>
Land Rover hopes it can achieve a 30 per cent fuel savings with hybrid 
technologies specially developed for SUVs. The CO2-reduction package includes an 
engine stop-start system, electric drive to the rear axle and an indigenous 
propshaft that disconnects the drive when not needed. This is being showcased on 
the Land_e concept. “The Land_e technology will be applicable to our entire 
range, both gasoline and diesel,” commented managing Director Matthew Taylor. 
Land_e could bring the CO2 output of an SUV such as the Freelander down to below 
150 grams per kilometer. One of the first elements to be applied is likely to be 
the belt-driven starter-generator. This could come within two years and would be 
suitable for the new Freelander.</font></p><hr width="80%" size="1">
