<br>
</font><b><font face="Verdana" style="font-size: 14pt">The little giant!</font></b><font face="Verdana" size="1"><b><br>
H3, the latest addition to the Hummer clan, stands apart for the smoothness and 
driving comfort, writes Anne Kurien</b></font></p>
<p align="center" dir="ltr"><font face="Verdana" style="font-size: 11pt"><b>
<img border="0" src="cartalk.jpg" width="331" height="196"></b></font></p>
<p align="justify" dir="ltr"><font face="Verdana" style="font-size: 11pt"><b>A</b></font><font face="Verdana" size="1">fter 
seeing the gargantuan H1s and H2s on the road, it can be tough to imagine a 
similar version in a smaller size. But General Motors mandarins have rewritten 
the rules and brought out a smaller, fitter, and yet, power-packed version of 
the Hummer clan — the H3. Die-hard fans of H1 and H2 might wonder if there was a 
need for something smaller. After all, isn’t the Hummer all about being the out 
and out off-road vehicle, with the bulk, power and the indomitable military DNA 
to boot?<br>
<br>
Have no doubts, GM knows what it is doing, even if it means bringing out a 
gentler and downsized version of the H1 and H2. For, if the sales in the home 
market, the United States, and even Oman are to be taken into account, H3 seems 
to be the next bestseller for the global auto giants. <br>
<br>
In a nutshell, here’s what makes the H3 tick: the same unique Hummer styling in 
a compact version, with the legendary off-road capabilities and a starting price 
that’ll blow your mind away. To top it, H3 is the first Hummer to offer manual 
transmission as opposed to H1 and H2, which come only with automatic 
transmission. And this time round, it’s not just the males that the H3 targets 
at, but also the females, who like to rough it out and want to add some fun to 
their driving. <br>
<b><br>
The Exterior Story </b><br>
When the identity of a vehicle like Hummer is based on its imposing size and 
power, bringing out a smaller model can be slightly dicey. But the manufacturers 
know where to draw the line. The H3 has only gone down a few inches in height 
and in length, keeping the family genes intact. Size-wise, compared to the H2, 
H3 is 16.9 inches shorter in length, 6 inches shorter in height and 6.5 inches 
narrower. H3 sports the same striking lines, 32-inch all-terrain tires, the 
identifiable hood, famed Hummer grille, customary front tow hooks, low windows, 
and the macho metallic body as its predecessors.<br>
<br>
One of H3’s striking features is the extremely short front and rear overhangs, 
increasing its ability to negotiate Steep, uneven, and rocky terrain, an 
important feature when traversing the extreme off-road environments that the 
Hummers are used to. The H3 also comes with protective aluminum front skid 
plates. The large headlamps and fog lamps only add to the visual impact. Want to 
add more drama to this little giant? Get a set of roof-mounted lights.<br>
<b><br>
The Inside Story</b><br>
The first thought that comes to mind on getting into the H3 is clutter-free. The 
H3 boasts a simple, uncluttered dashboard and easy-to-operate controls. Though 
the H3 is designed to perform well both off-road and on-road, the controls have 
undoubtedly been ergonomically fashioned, keeping comfort driving in mind. 
Unlike a few of its ilk, the H3 does not cut on the pleasure of driving, with 
complicated and hard-to-decipher controls. This is one vehicle where driving 
experience scores higher than the fancy interior equipment. <br>
<br>
H3 is fitted with front bucket seats that offer great lumbar support — crucial 
for your off-road maneuvering. Space is of no dearth in the H3 for its size. The 
fact that the spare tire hangs on the rear door has increased room on the 
inside. <br>
<br>
Even if you are not used to narrow windows in larger vehicles, which can give a 
claustrophobic feel, the H3’s unique styling takes away any such discomfort. The 
H3’s low glass to metal ratio makes it a safe automotive as the center of 
gravity goes down considerably. If you are one for sunroof, you will find it in 
the advanced version of the H3.<br>
<b><br>
On and Off the Road </b><br>
The H2, which came a few years before the H3, is more of an off-road vehicle and 
the H1 is the original “most serious four by four on earth” which actually isn’t 
really an exaggeration. The H2 can be relied to perform well on rough terrain 
and under varied weather conditions. The H3 has been built to hold its place 
among the older siblings. Hummer’s famed engineering come alive as soon as you 
hit the road. It has been fitted with a Vortec 3500, 3.5-litre engine with 220 
hp, giving the ultimate Hummer experience. <br>
<br>
The iconic brand is known for its all-terrain driving capabilities. But where 
the new offering stands apart is the smoothness and driving comfort — even on 
the tarmac. If you thought Hummers were meant for desert safaris and rocky 
hillsides, then the H3 will catch you by surprise, like it did us! This small 
monster is as easy to handle on the road, as it is ploughing on the roughthrough 
boulders. If the earlier versions from Hummer were used more for off-road 
jaunts, the H3 is one vehicle that you can take not just to the outdoors but 
also to your office, to pick up your kids after school and take your wife for 
her monthly shopping as well. (You’d have to do that at your risk for there’s no 
guarantee she’s going to return the H3 once she’s been behind the wheel!)<br>
<br>
This is one vehicle that is going to rewrite the rules as far as woman drivers 
are concerned. There’s no more turning away or standing in awe on the sight of a 
Hummer where the ladies are concerned. The H3 has been built with women in mind 
too. Even though it’s smaller in size compared to the H1 and H2, the H3 is 
muscular and strong as ever. However, the feeling of being in control behind the 
wheel of the H3 is distinctive. It will surely appeal to a lot of woman drivers.
<br>
<b><br>
Safe and Sound </b><br>
Being inside a Hummer keeps half your safety worries at bay. The feeling of 
security inside the H3 is immense and that’s even before we get to know the 
safety features. Thankfully, we did not have to put to test any of the safety 
features, though these are a few highlights that gave the H3 our points. <br>
<br>
The H3 comes with dual airbags and a Passenger Sensing System that turns off 
automatically if the front seat passenger is a child. The H3 also comes with a 
tire pressure monitoring system — important when you are roughing it out on the 
tougher stretches. <br>
<br>
As with the other Hummers, the H3 also boasts all-steel body panels and an 
exceptionally strong frame, which protects occupants in the event of a 
collision. The H3 wins our votes as an-all purpose vehicle and will surely hold 
its own ground. And when a Hummer comes for as low as RO11, 900 (manual 
transmission), there’s no doubt that it is a killer.</font></p>
