</font><b><font face="Verdana" style="font-size: 14pt">
Nurture to motivate</font></b><font face="Verdana" size="1"><br>
<b>It’s important to tap the potential of your team, writes Sunita C Gomes</b><br>
<br>
<img border="0" src="byinvet1.jpg" width="100" height="157" align="left" hspace="5" vspace="2"><br>
<i><b>About understanding….</b><br>
It is the province of knowledge to speak, and it is the privilege of wisdom to 
listen.<br>
— Oliver Wendell Holmes<br>
</i><br>
The 
key to motivating people, to harnessing the energies of a group of people who 
are pursuing their individual goals and dreams, is to listen to them, one by 
one. And that means understanding each one, as a unique individual. They are 
already motivated: your only question is, by what?<br>
<br>
People are like plants. To bloom and thrive they need to be rooted in the rich 
soil of a nurturing environment. Yet we find many a manager rich in knowledge 
and unskilled in the art of human horticulture — listening, understanding and 
communicating.<br>
<br>
Understanding others is not like understanding facts. Understanding others 
engages both the head and the heart. Generally, true understanding comes with 
time and does not happen overnight. Yet, there are exceptions to this rule. If 
you and I have shared interests, or if we have had similar life experiences, 
understanding comes more readily. <br>
<br>
Abraham Lincoln lost his third son during the Civil War. His wife took to her 
bed and was never the same thereafter. In other words, with his son’s death, 
Lincoln lost his family. Thousands of families lost their loved ones during this 
War — and Lincoln understood in his heart, and with empathy, the magnitude of 
this loss.<br>
<br>
Some people are like water in a faucet. It is already motivated to flow, but it 
doesn’t have the opportunity until you open the tap. Your team member may be 
bursting with energy and waiting only for you to provide her the opportunity to 
use it constructively.<br>
Others are like mountain streams, which flow swiftly but follow their own 
courses. If you want them to turn your wheel, you have to provide them with a 
channel. <br>
<br>
Remember people do not do things for your reasons. They act for their own 
reasons. Your challenge is to open up inviting opportunities that will focus 
their energies on your collective goals.<br>
<b><br>
Cultivate understanding via communication</b><br>
“The more elaborate our means of communication the less we communicate.” (Joseph 
Priestley). Fair or not, communication with people under our watch — whether by 
e-mail, cell-phone or face to face — sets the tone for the entire staff. 
Regrettably, employees generally view bosses through a distorted lens; this 
gives them an opportunity to downplay the good side and exaggerate the bad. When 
job frustration hits critical mass, they can feel justified in checking out 
emotionally. <br>
<br>
Unfair as it is, too many managers perpetuate the stereotype. Let go of the 
notion that signs of emotion or fallibility are unprofessional. Hand out 
all-access passes. Make yourselves available to your team when they need you to. 
Their appreciation will show up in the simple metrics of productivity and 
turnover. <br>
<b><br>
View mistakes as opportunities</b><br>
The story goes that Thomas Edison and his crew worked 24 hours straight to 
complete the first working light bulb. Exhausted, Edison placed the precious orb 
in the hands of his young assistant to carry upstairs. The nervous boy was 
shaking so badly that he dropped the bulb on the way up. <br>
<br>
The team spent another 24 hours making a second bulb. When the time came to 
carry it upstairs, Edison smiled and again handed it to the boy. Confidence 
restored, the kid ferried the bulb upstairs without incident. The simple gesture 
earned Edison the boy’s loyalty for a lifetime. <br>
<b><br>
The Platinum Rule</b><br>
We all know the Golden Rule: Do unto others as you’d have them do unto you. This 
is good advice, but those who understand it effectively use the Platinum Rule: 
Do unto others as they want to be done unto.<br>
<br>
Often, the best way to determine how people want to receive your message is to 
take a look at how they communicate their own messages to you. The more your 
communications are able to mirror back to the recipients their own likes and 
preferences, the more likely they are to ‘vibe’ with you and respond quickly and 
positively to your understanding.<br>
<br>
As we plunge ever faster into a sea of change, leaders will be seen more and 
more as problem solvers in business, politics and education. Leadership will go 
beyond the academic concept to a paradigm that is born in service, contribution, 
stewardship, courage, listening and understanding. So, get ahead of the curve. 
Encourage your children to study it, practice it and pursue mastery in 
communication and understanding.<br>
<br>
<i>The writer is Country Manager of British Airways in Oman &amp; Yemen. (The views 
of the author are independent and do not reflect the views of the company she 
represents in Oman)</i></font></p>
