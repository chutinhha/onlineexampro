    <p style="margin: 10px" align="justify"><font face="Verdana" size="1"><b>Dubai issues property law</b><br>
    The Dubai Government, in a historic move, issued a long-awaited property law 
    that legalises foreign ownership of properties in pre-designated areas in 
    the emirate. His Highness Sheikh Mohammad Bin Rashid Al Maktoum, 
    Vice-President and Prime Minister of the UAE and Ruler of Dubai, issued Law 
    No. 7 that legalises freehold ownership of land and property for UAE and GCC 
    citizens, while allowing the same rights to non-GCC expatriates to 
    pre-designated areas that will be approved by the Ruler of Dubai.<br>
    <br>
    Non-GCC expatriates will be given the right to acquire freehold and 99-year 
    lease property in designated areas. The law comes nearly four years after 
    the government first announced freehold ownership to expatriates, grouped 
    under three Dubai-government owned entities Emaar Properties, Nakheel and 
    Dubai Properties. <br>
    <br>
    The new law paves the way for expatriate homeowners to register their 
    properties in their names with Dubai Lands and Properties Department, which 
    has been gearing up for the rush in registration. The law will be followed 
    by a number of new bylaws that will identify the freehold areas in Dubai and 
    determine the registration fees and procedures. More than 13,000 expatriate 
    families have already moved in to their new homes, without securing title 
    deeds in their names, while another 7,000 are expected to move in by the end 
    of the year. They all will benefit by the new law.</font></td>
