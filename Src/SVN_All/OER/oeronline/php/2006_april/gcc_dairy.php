<br>
</font><font face="Verdana" style="font-size: 14pt"><b>High on construction<br>
</b></font><font face="Verdana" size="1"><b><br>
UAE leads the pack in the booming real estate market in Gulf States<br>
</b><br>
</font><font face="Verdana" style="font-size: 11pt"><b>T</b></font><font face="Verdana" size="1">he 
Gulf has been experiencing monumental construction activity over the last few 
years. Both public and private sectors are equally involved in construction. 
Ambitious projects are underway. They range from infrastructure buildings, 
industrial parks, commercial property, and shopping malls to residential 
developments.<br>
<br>
Increasing oil revenues, supported by freehold property laws brought in by 
various Gulf Cooperation Council (GCC) countries, are giving fillip to the 
construction industry. Some of the prestigious construction projects of the 
region include Dubai Light Rail — the world’s longest metro system, Saudi 
Arabian Rabigh refinery, The Wave in Oman, the UAE’s Burj Dubai — world’s 
tallest residential tower, the Palm in the UAE — man-made islands, the Sohar 
Aluminium smelter in Oman, RasGas III, Pearl-Qatar in Qatar, Seef — 30-storey 
residential Avare Towers, and Financial Harbor in Bahrain.</font></p>
<p align="justify" dir="ltr"><font face="Verdana" style="font-size: 11pt"><b>
<img border="1" src="gccdai.jpg" width="250" height="283" align="left"></b></font><font face="Verdana" size="1">The UAE dominates the Gulf construction sector with US$ 35.562 billion worth of 
projects under construction in 2005, accounting for 63.7 per cent of the total 
value of projects under construction in the GCC states. Saudi Arabia occupies 
the second slot with US$ 7.869 billion, with Qatar US$ 4.596 billion in third 
place followed by Kuwait US$ 9.254 billion, Bahrain US$ 1.99 billion and Oman 
US$ 1.388 billion, according to a study commissioned by the Abu Dhabi National 
Exhibitions Company (ADNEC). <br>
<br>
The study further reveals that as of June 2005, there were a total of 1,825 
building construction projects being implemented within the GCC. The UAE leads 
the market with 820 projects, accounting for 46 per cent of the total. 
Currently, 214 building construction projects are underway in Bahrain, 209 in 
Qatar, 208 in Saudi Arabia, 207 in Kuwait and 167 in Oman. Leisure developments 
totaling US$10 billion are under way/planned in the Gulf States in the next 
decade in response to the high level of internal and external tourism in the 
region.<br>
<br>
With UAE dominating the GCC construction market, about 60 per cent of the total 
construction spend in GCC will also take place in this country alone. 
Dubai-based large property developers Nakheel, Emaar, Dubai Properties and Dubai 
international properties have announced projects worth billions, taking UAE’s 
share of investment to US$ 221 billion, according to the MEED report. Emaar 
alone is working on nine industrial projects worth US$ 1 billion. Government 
sources expect that US$ 50 billion will be invested in the real estate sector 
during 2004-2010 in Dubai.<br>
<br>
Bahrain also experienced ups and downs in the construction and real estate 
industry. After a decline in performance during 1999 to 2001, the industry 
picked up steadily and in the first half of 2004, 171 and 341 licenses were 
issued for the construction sector and real estate, rentals and associated 
activities sector respectively. In 2003, the industry recorded a staggering 114 
per cent growth over 2002. In the Kingdom of Saudi Arabia, annual investments in 
construction industry have touched an average of US$ 19.99 billion between 1999 
and 2003. </font></p>
<p align="justify" dir="ltr">
<img border="0" src="gccdai1.jpg" width="200" height="130" align="right" hspace="5" vspace="2"><font face="Verdana" size="1">In the last few years, construction sector is contributing heavily to the GDP of 
the GCC countries. On an average, real estate and construction sectors make up 
almost 10 per cent of the total GDP in the region. In Qatar, the construction 
sector’s contribution to GDP has witnessed a 73 per cent jump between 2002 and 
2005. In the UAE, the growth has been 40 per cent for the same period. For 
Bahrain, construction sector has contributed US$ 368.35 million (3.85 per cent 
of GDP) in 2003, as compared to US$ 355.97 million in 2002 (4.2 per cent of 
GDP). Real estate and business services contributed US$ 850.385 million in 2003 
(8.9 per cent of GDP), as compared to US$ 821.5 million in 2002 (9.8 per cent of 
GDP).<br>
<br>
The increase in demand for low and middle-end housing in the last few years has 
led to drastic increase in property prices in the region. Land prices as a 
proportion of the total project cost on an average increased to as high as 25 
per cent in Kuwait and Qatar. High land prices may push up cost of many of 
ongoing projects.</font></p>
