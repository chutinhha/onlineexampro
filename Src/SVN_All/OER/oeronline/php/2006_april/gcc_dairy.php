<p align="justify" dir="ltr"><font face="Verdana"><b><font style="font-size: 14pt">Gold rush</font></b></font><font face="Verdana" style="font-size: 14pt"><b><br>
</b></font><font face="Verdana" size="1"><b><br>
</b>Increasing gold rates have had no negative effect on the sales and demand 
for gold. The Gulf region’s enthusiasm for gold has not reduced. In 2005, the 
demand was strong in the Middle East with 9 per cent growth for the region and a 
new record for jewelry off-take in the United Arab Emirates (96 tonnes).<br>
<br>
Increasing construction activities and passionate tourists have helped in 
boosting gold trade. While gold demand increased by 13 per cent in Saudi Arabia, 
it rose by just 5 per cent worldwide in 2005. Saudi Arabia showed the strongest 
overall growth for the region with jewelry demand rising by 12 per cent and 
overall demand by 13 per cent. Despite a fall in fourth quarter, the UAE also 
recorded 8 per cent increase in jewelry demand Jewelry buying in the rest of the 
Gulf region, including Kuwait, Bahrain, Qatar and Oman, was 5 per cent higher in 
2005 compared to 2004 with the price rise in Q4 having only a limited impact on 
buying. The increase in gold demand prevailed in the Kingdom’s neighboring Gulf 
countries, including Turkey and Egypt.<br>
<br>
Price volatility, rather than increase in price, in the last quarter of 2005 was 
the prime deterrent to purchase. The impact of the price rise in Q4 2005 was 
also more muted in Saudi Arabia than in the rest of the region with jewelry 
demand dropping by just 3 per cent on Q4 2004.<br>
<br>
<img border="0" src="gccdai3.jpg" width="203" height="37"><br>
Without the support of oil, gas and mineral expansion sectors, Saudi Arabia 
managed to attract US$ 26.046 billion cumulative foreign direct investments in 
2005. According to the preliminary results shown in a report recently released 
by Saudi Arabian General Investment Authority (SAGIA), the total foreign and 
joint investments executed are estimated at US$ 8.104 billion, with a growth 
rate of 95 per cent compared to 2004 when the actually executed investments 
reached US$ 4.158 billion.<br>
<br>
Amr Abdullah Al-Dabbagh, Governor, SAGIA said the Kingdom registered a 30 fold 
increase in foreign investments, especially, overseas investments. In 2005, it 
totaled US$ 53.32 billion. Even the International Finance Corporation in its 
“Doing Business in 2006” report placed Saudi Arabia ahead of Kuwait and Oman and 
38th out of 155 locations worldwide for investment.<br>
<br>
Japan leads the table of top investors in the Kingdom followed by the UAE, 
Bahrain and the US. Japan invested around US$ 2.319 billion in the Kingdom 
during 2005. However, the US is in the forefront when cumulative investments are 
compared until the end of 2005. Investments of the US are worth US$ 9.421 
billion for 265 projects, followed by Japan with US$ 4.561 billion in 35 
projects, and the UAE with US$ 3.113 billion in 84 projects.<br>
<br>
To encourage foreign investment further, the Kingdom is studying a tax credit 
formula, said Al-Dabbagh. The other initiatives under consideration include 
setting up of a re-export zone to encourage entrepreneurs. Raw materials brought 
in for manufacturing would be exempted from customs duty, which would be imposed 
on the finished products only.<br>
<br>
<img border="0" src="uae.jpg" width="206" height="39"><br>
This year’s no-deficit budget was the first integrated one in 25 years that is 
based on the three-year performance-budgeting system (PBS), and is the second 
balanced budget in 25 years introduced in the United Arab Emirates. The UAE is 
the first country in the region to implement the PBS in its financial and 
accounting system as per international standards.<br>
<br>
<img border="0" src="gccdai2.jpg" width="208" height="156" align="right" hspace="5" vspace="2">The 
UAE Cabinet has announced its second balanced federal budget which has a revenue 
outlay of US$7.569 billion, 23 per cent higher than the previous year. The 2006 
budget focuses on education, health, social services and projects. The budget 
has balanced expenditure and revenue, both totaling US$ 7.59 billion. <br>
<br>
The 2006 budget was approved by the Cabinet. “For the second executive year, the 
budget achieved a zero-deficit,” Dr Mohammad Khalfan Bin Kharbash, Minister of 
State for Financial and Industrial Affairs, said. The budget gives to priority 
in spending to public and higher education, with the allocation of 36.6 per cent 
for this key sector, or about US$ 1.966 billion, compared with US$ 1.82 billion 
in 2005.<br>
<br>
The government has allocated 16.8 per cent (US$ 880 million) for the security 
sector, 8.5 per cent or US$ 443.84 million for the health sector, an increase of 
42 per cent over last year, while social affairs received 7.8 per cent (US$ 
408.45 million). Infrastructure allocation stood at US$ 364.88 million (7 per 
cent) while project sector allocations totalled US$ 326.76 million (6.3 per 
cent) compared with US$ 239.62 million last year. The increase in education and 
health allocations came in line with the directives of President His Highness 
Shaikh Khalifa Bin Zayed Al Nahyan, and Shaikh Mohammad. <br>
<br>
Bin Kharbash stressed that the 2006 budget does not envisage any increases in 
fees for services offered by the federal government. The increase in allocations 
for education is aimed at developing the sector by introducing modern technology 
applications and to help students develop their educational and scientific 
skills. The additional US$ 68 million added to the health sector allocations was 
aimed at meeting the increasing requirements of hospitals and medical 
institutions for equipment, medicines and staff. <br>
<br>
<img border="0" src="kuwait.jpg" width="193" height="42"><br>
Recent estimations by the Public Authority for Civil Information (PACI) reveal 
that the total population of Kuwait stands at 2.75 million at the end of 2004. 
Population growth accelerated to an annual rate of 8 per cent as the number of 
expatriates rose sharply in 2004. Nearly 1.79 million or 66 per cent are 
expatriates, while 956 thousand are Kuwaiti nationals. The number of non-Kuwaiti 
residents increased by 11 per cent from a year ago compared to 6.4 per cent in 
2003. Growth of the Kuwaiti segment slowed down slightly to 3.1 per cent.<br>
<br>
Non-Kuwaitis dominated the work force, representing 82.2 per cent of the total, 
at the end of 2004. Expatriate labour force increased by 12 per cent annually. 
However, the number of working Kuwaitis also increased 6.6 per cent from the 
previous year, the fastest rate of growth in seven years.<br>
<br>
<img border="0" src="bahrain.jpg" width="201" height="36"><br>
The Industry and Commerce Ministry has envisaged a detailed blueprint for the 
industrial sector that is expected to triple the industry’s contribution to 
Bahrain’s economy — from 12 per cent to 36 per cent in 10 years — and create 
30,000 new jobs, with competitive salaries, in the same period.<br>
<br>
The plan aims at making Bahrain a world-class industrial hub. The plan will 
focus on export of non-traditional items as well as global competition, with a 
view to becoming a hub of highly profitable, knowledge-based and 
environment-friendly industries. The Minister of Industry and Commerce, Dr 
Hassan Fakhro, informed the Shura Council: “Our goal is to ensure that GDP grows 
to 8.6 per cent per year from the present 7.1.”<br>
<br>
The Kingdom will diversify into various industrial and manufacturing activities 
such as the manufacturing of aircraft parts and auxiliary equipment, production 
of soaps, detergents and paints and analysing their export potential within the 
Gulf. The policy also covers electric train and car spare parts, transportation 
equipment, computers and electronics items. A high-tech fashion industry will be 
developed to compete at the regional and international&nbsp; levels. But the 
textiles sector must “refocus on the product mix” to gain and retain foothold in 
the international market, according to the minister. <br>
<br>
To achieve the target, the ministry was working to make changes in legislations 
to ensure more businessmen were encouraged to invest in industries. “The 
strategy brings businessmen and the government together to help raise the 
industrial sector,” the minister added. For smooth policy implementation, a 
business support centre will provide information and advice on all aspects of 
planning and technology transfer. And a productivity and competitiveness centre 
will work on increasing output of new and existing units. “What we need is a 
business-friendly legal system to support innovation, entrepreneurship and 
business operations. With major reforms in the education and health sectors, we 
can have a world-class environment for a competitive and knowledge-based 
industrial sector,” he said.<br>
