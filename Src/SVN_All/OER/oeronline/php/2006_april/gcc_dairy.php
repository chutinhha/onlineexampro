The Middle East’s first energy business hub, Energy City Qatar (ECQ), was 
officially launched recently. It will be the region’s first full-service energy 
business centre catering to the commercial, technical and human resource needs 
of the oil and gas industry operating in the region, with cutting edge 
facilities and services. <br>
<br>
<img border="0" src="gccdai4.jpg" width="69" height="119" align="right" hspace="5" vspace="2">The 
Middle East accounts for over 60 per cent of the world’s proven oil reserves and 
over 40 percent of the world’s natural gas reserves. But, the region doesn’t 
have a dedicated energy business centre. ECQ was conceived with the central aim 
of filling this void and positively contributing to the economic development of 
Qatar and the region.<br>
<br>
Envisioned by Gulf Energy, ECQ aims to attract the industry leaders in Oil and 
Gas production, International Oil Companies, National Oil Companies, support 
services, infrastructure and downstream activities, shipping and trading, market 
and resource data, intellectual property and energy trading. It will also be 
home to a dedicated energy trading platform, the International Mercantile 
Exchange (Imex), which will be regulated by the Qatar Financial Centre 
Regulatory Authority. It will be an innovative, intelligent, vibrant energy 
centre filled with customers, suppliers and related service providers freely 
interacting, cooperating and competing for the advancement of energy sector.<br>
<br>
ECQ forms part of the major new city development, Lusail, which in addition to 
major business and entertainment districts, will be home to up to 200,000 
residents. ECQ’s lead financial advisor is Gulf Finance House, and PFC Energy, 
the leading global energy consultant, is the strategic advisor. Its strategic 
partners are Abu Dhabi Investment House and Kuwait Investment Company.<br>
