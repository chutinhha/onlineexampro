                            <font face="Verdana" size="1"><b>Shell Helix 
                            co-sponsors expedition</b><br>
                            The Shell-Helix co-sponsored Biosphere Expeditions 
                            team concluded its survey of the Arabian leopard 
                            among the remote mountains and gorges of Musandam. 
                            This is significant since the Arabian leopard is the 
                            last surviving species of the Big Cats on the 
                            Arabian Peninsula and is a severely endangered 
                            species. Setting out in the middle of January, the 
                            team primarily looked for evidence of leopard 
                            presence at hotspots like waterholes.</font></p>
