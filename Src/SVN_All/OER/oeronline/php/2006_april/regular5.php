    <p align="center" style="margin: 10" dir="ltr">
    <b><font face="Verdana" size="3">The media quagmire</font></b><p align="justify" style="margin: 10" dir="ltr">
    <font face="Verdana" size="1">For a change, the media and communications 
    industry itself was in the limelight last month. In the beginning, there was 
    the Middle East Publishing Conference, and later, the entire global 
    advertising fraternity congregated for the IAA World Advertising Congress. 
    The twin events, held in Dubai, underlined the fact that along with the more 
    talked about fast growing industry sectors, the media and communications 
    industry is also developing rapidly in the Middle East. <br>
    <br>
    More than 70 new magazines, 14 TV channels, nine radio stations and several 
    newspapers were launched in 2005 in the region. As per the Middle East Media 
    Guide 2006 Edition, there are more than 700 magazines, 170 newspapers, 140 
    TV stations and 145 radio stations across 14 countries in the region. The 
    launch of Dubai Media City in 2000 set the ball rolling by liberalizing the 
    market to kick-start new ventures in hordes. This phenomenon had a spiral 
    effect leading to the entry of new players and expansion by existing 
    companies in other Gulf countries. <br>
    <br>
    With the advent of new titles, the advertising industry has also expanded 
    exponentially. As per market estimates, at least three countries (the UAE, 
    Egypt and Saudi Arabia) from the Middle East are expected to be among the 
    world’s top 10 generators of advertising revenue between 2004 and 2008. The 
    ad spend in the Gulf countries is expected to cross US$5 billion this year.
    <br>
    <br>
    The advertising market here is more skewed towards the print media. Contrary 
    to the global trends, newspapers corner a lion’s share (51 per cent) of ad 
    revenues in the region. The global average is 30 per cent. TV medium 
    accounts for 30 per cent of the ad revenues in the region against the global 
    average of 37 per cent. But sooner or later, the region is going to follow 
    the global trends as we get into the consolidation and rationalization mode 
    once the boom subsides. <br>
    <br>
    Though it is heartening to know that the regional media industry is 
    galloping ahead, it is still stuck in the quagmire of issues, which need to 
    be resolved. Despite sharp increase in the number of newspaper titles, only 
    50 out of 1,000 people read newspapers in the region. This is extremely low 
    when compared to the global standards. Timothy Balding, Director General of 
    World <br>
    Association of Newspapers (WAN), blamed the media environment for this trend 
    as he said: “The content of regional newspapers may not be in tune with what 
    readers are looking for.” His views may be correct as the regional press 
    fails to give the comprehensive picture many a time — for obvious reasons — 
    in an age when the latest information (generated in some other part of the 
    world) is available at the click of a mouse! <br>
    <br>
    Then, there is this contentious issue of ‘media circulation and readership 
    auditing’. As per estimates, the market research spending in the region has 
    gone up by 35-40 per cent, but only a fraction of it goes into media-related 
    research. In the absence of dependable data, the advertisers and agencies 
    rely more on their gut feel in taking a decision on media buying for their 
    ads. This practice is almost unheard of in developed markets. <br>
    <br>
    As the market is maturing, there is definitely a need for developing 
    standard industry practices, which could address this problem. As the Middle 
    East market is different from other markets, from where the media audit 
    model has originated, the industry needs to standardize and localize the 
    audit system to suit the requirements of the regional market. The loopholes 
    that exist in the current audit system need to be plugged so that the blame 
    game is put to rest. The system has to be made relevant and accurate before 
    it becomes a norm. </font></td>
