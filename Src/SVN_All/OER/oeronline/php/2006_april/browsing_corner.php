            <br>
            </font><font face="Verdana" size="4">
            Rethinking the future</font></b><font face="Verdana" size="1"><br>
            <br>
            </font><font face="Verdana" style="font-size: 11pt"><b>S</b></font><font face="Verdana" size="1">ix 
            powerful business strategies and pearls of wisdom from as many as 15 
            leading business and management ‘gurus’! And all this stuff in one 
            book! Rethinking the Future makes absolutely invigorating reading in 
            the face of rapid changes in the economic, technical and social 
            fields, which have shattered old ways of doing business the world 
            over — not only for private companies and public corporations but 
            also the way in which governments steer their economies.<br>
            <br>
            <img border="0" src="brokor.jpg" width="220" height="147" align="left" hspace="5" vspace="2">Vertical integration and economies of scale, both cornerstones of 
            industrial growth, are now giving way to outsourcing, networks and 
            profit centers. Rowan Gibson, considered as a ‘guru among gurus’, 
            couldn’t have better edited these stimulating, thought provoking and 
            sometimes, controversial views. As he puts it: “There is no yellow 
            brick road to the future, no quick short-cut, no one best way… The 
            purpose of this book is to help you make the right choice. To assist 
            you in rethinking and then shaping the future, before it shapes 
            you.”<br>
            <br>
            The first step to rethinking the future, then, is rethinking the 
            principles themselves. According to Charles Handy, Britain’s 
            foremost business philosopher, best known for his ideas on changing 
            nature of work and organizations, the excitement of the future lies 
            in the fact that we can shape it. Stephen Covey, author of the best 
            selling book, Seven Habits of Highly Effective People, is of the 
            opinion that, “tomorrow’s successful leaders will value principles 
            more than they value their companies.” It is through such highly 
            held principles that some companies have been established, like for 
            instance The Body Shop, which does business with a conscience.<br>
            <br>
            Rethinking competition begins with views from two Professors of 
            Business Administration — Michael Porter of Harvard Business School 
            and C K Prahalad of University of Michigan. According to Porter, the 
            important thing is to shape the nature of competition and take 
            control of one’s destiny. He outlines a fresh look at competitive 
            strategy for all firms, especially small companies, and pitfalls 
            encountered by larger firms, importance of innovation and local 
            competition.<br>
            <br>
            C K Prahalad feels competing for the future means maintaining 
            continuity by constantly creating new sources of profit. He feels 
            that the traditional assumptions of clear industrial boundaries, 
            industries with distinct characteristics and the assumption that you 
            can plan for the future, no longer hold true. It is now not enough 
            to imagine the future — you have to also build it! “Many companies 
            have had incredible foresight but they lacked the capacity to 
            execute it.” According to his co-author, Gary Hamel, “you cannot 
            create the future by using old strategy tools.” Simply catching up 
            isn’t enough. You have to invent new games with a lot of 
            imagination.<br>
            <br>
            Contributions to rethinking leadership come from Dr. Warren Bennis 
            and John Kotter. Good managers are not enough; there have to be 
            leaders. Today’s organizations have many bright sparks — all under 
            one roof. The task is to harmonize them. And that requires “leaders 
            of leaders”, people with vision and meaning.<br>
            <br>
            Kotter explains why it’s good to have cultures that enable us to 
            adapt to changing environment rather than have corporate cultures 
            that are anchors on change. For all this to happen rethinking 
            control and complexity in organizations is important. Contributions 
            from Michael Hammer, Eli Goldratt and Peter Senge will leave no room 
            for doubt that in the post-industrial era, organizations and 
            individuals need to re-engineer and be empowered differently to suit 
            changing trends. Personal development, cultural differences, 
            creating a learning infrastructure, sharing knowledge, 
            interrelationships and interdependencies, and rethinking hierarchies 
            are some of the points discussed by these leading management 
            specialists.<br>
            <br>
            A constantly changing business environment makes it essential for 
            managers to rethink markets and look into changing attitudes and 
            demands of new kinds of consumers in new kinds of markets. Al Ries 
            and Jack Trout, arguably the world’s best known marketing 
            strategists, give insights into staying focused in such a fuzzy 
            world. Dr. Philip Kotler gives tips on mapping the future 
            marketplace.<br>
            <br>
            The book concludes with views from John Naisbitt, Lester Thurow and 
            Kevin Kelly, who talk about the unprecedented changes taking place 
            worldwide and the impact of ‘network economics’ on every aspect of 
            business. To quote Yogi Berra, “The future ain’t what it used to 
            be.” So, don’t think business as usual. Rather grab a copy and 
            prepare for an ever-changing future!<br>
            <br>
