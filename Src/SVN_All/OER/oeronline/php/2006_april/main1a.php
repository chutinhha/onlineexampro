    Advanced mooring system for Salalah</b><br>
    New Zealand-based Mooring Systems Limited (MSL) has been awarded a contract 
    by Salalah Port Services (SPS) to supply mooring systems designed to 
    mitigate the effect of waves on container ships at berth during the khareef 
    season. Under the latest deal, MSL will supply four MoorMaster 600 units. 
    The units will be install ed and commissioned in June this year just before 
    the onset of the khareef season. <br>
    <br>
    It will provide four automated mooring units designed to secure some of the 
    largest container ships while at berth at the transshipment terminal. The 
    deal follows the successful trial of a pair of mooring units in Salalah last 
    year — the first time that MSL’s innovative systems have been tested at an 
    international container port. The objective of the trial was to evaluate the 
    impact of automated mooring within the port, as well as assess the 
    performance and ability of MSL’s mooring technology to dampen vessel 
    movements while moored with the systems.<br>
    <br>
    Vessels subjected to long waves and moored conventionally with ropes often 
    surge alongside the piers, a phenomenon generally witnessed during the 
    khareef season. This vessel motion adversely affects container transfer 
    rates and the productivity of the terminal and ships. However, ship motions 
    caused by long waves were substantially reduced with MSL’s automated mooring 
    units, even when deployed alongside vessels of 8,000 TEU capacity and 98,000 
    tonnes displacement.</font></td>
