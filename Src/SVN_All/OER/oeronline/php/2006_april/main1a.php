<p align="justify" dir="ltr" style="margin-left: 5; margin-right: 5"><br>
<b>
<font face="Verdana" style="font-size: 14pt">Ride or fly to Sohar!<br>
</font><font face="Verdana" style="font-size: 8pt">An airport at Sohar is going 
to become a reality as the Government 
floats a tender to appoint consultants for the project</font></b></p>
<p align="justify" dir="ltr" style="margin-left: 5; margin-right: 5">
<font face="Verdana" style="font-size: 11pt"><b>
<img border="0" src="be_mus1.jpg" width="200" height="105" align="right" hspace="5" vspace="2">T</b></font><font face="Verdana" size="1">he 
day is approaching when one might reach Sohar in less than 30 minutes! Serious 
plans are afoot for the construction of an airport in Sohar. The tender inviting 
bids from multinational firms to offer consultancy services for the 
multi-million Rials airport is already out. <br>
<br>
The next few weeks are going to be hectic for prospective bidders as well as the 
Ministry of Transport and Communication that will oversee this massive exercise. 
Ever since the government declared its intentions to set up three new airports — 
at Sohar, Duqum and Ras al Hadd — the officials are burning the proverbial 
midnight oil outlining the details for the proposed airport at Sohar among other 
places.<br>
<br>
Given the pace at which the Sohar Port City and Sohar Industrial Estate are 
shaping up — involving huge foreign direct investment from global giants drawn 
from several corners of the world — an airport in Sohar has become a necessity. 
Though a well-maintained road provides excellent linkage, the three-hour ride is 
viewed as excessive. Sohar, once the capital of Oman and the historical shipping 
hub between east and west is coming back to life thanks to new projects which 
are in varied stages of planning and completion. <br>
<br>
“An airport will be a boon,” says Premchandran of Al Khaleej Polypropolene. For 
many operating out of Sohar, the proposed air link is not a luxury but a 
necessity. While it may take anywhere between 24-36 months for an airport to 
become fully functional, a basic runaway, with a fully equipped control tower, 
can be put together much faster. <br>
<br>
There is a market for a helicopter service between Muscat and Sohar. Many global 
giants, engaged in building their respective manufacturing plants, could easily 
make provisions for a helipad atop their own premises. But the issue at stake is 
a general airstrip, which can service all kinds of flights — and not just 
helicopters and small aircraft. In light of the fact that most of the operators 
touching Seeb International (Muscat) will look to touching down at Sohar as well 
if the traffic is good. Since Sohar will be catering to the business community, 
airfare is a no issue. Oman Air and Gulf Air may be the frontrunners given their 
heightened stake and interest in the Sultanate. <br>
<br>
While Seeb International Airport will continue to remain the main gateway to the 
Sultanate, Sohar airport — as and when it materializes — is bound to draw 
business traffic given its orientation in contrast to the touristic Salalah 
airport. <br>
<br>
The prospective bidders for the Sohar airport are meant to pick up their tender 
forms in the first week of April and submit their bids four weeks later. A clear 
picture as to who has won the prestigious contract will become clear in the 
third quarter of the current financial year if all goes according to schedule. </font></p>
