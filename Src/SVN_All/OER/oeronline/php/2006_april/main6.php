<p align="justify" dir="ltr"><b><font face="Verdana" style="font-size: 16pt">
<br>
</font><font face="Verdana" size="5">Reality TV: A success story</font></b><font face="Verdana" size="1"><br>
<b><br>
Following the western TV channels, the reality TV bug has hit the Arab TV 
channels in the GCC in a big way<br>
<br>
</b></font>
<b><font face="Verdana" style="font-size: 11pt">T</font></b><font face="Verdana" size="1">here 
was a time, not long ago, when viewers switched on TV for information and news. 
However, the trend has slowly, and steadily, shifted towards entertainment 
(movies, music and sports etc.). Now, the latest buzz of the industry is Reality 
Television (RTV). Though it is a new phenomenon for the Arab region, the US has 
more than 150 reality shows (excluding sequences), and the UK has about 22 
different reality shows broadcasting on various local and satellite channels.<br>
<br>
RTV’s attraction lies in its relative uniqueness to audiences as well as its 
lower costs of production, as the participants are mainly regular people and not 
highly paid movie and TV stars, Also writers aren’t needed, as it is for the 
main part unscripted. Currently, the most popular and controversial reality 
shows in the Arab region are Super Star and Star Academy. Usually, reality shows 
claim to be live and spontaneous. Hence the tag &quot;real&quot;! <br>
<br>
Like in any part of the globe, in the Arab world too, RTV is based on the aspect 
of emotional identification between the viewer and the protagonist, and appeals 
to a voyeuristic tendency. It also breaks the myths of perfection that 
celebrities try to propagate. <br>
<br>
Its success comes as yet another revenue source for Free-to-Air (FTA) satellite 
channels, especially where viewer participation is involved. Revenue sources 
from reality shows include sponsorships, advertising and interactivity revenues 
from viewer participation. <br>
<br>
<img border="0" src="tv1.jpg" width="300" height="256" align="right">These shows not only generate considerable income for the Arab channels, the 
really big winners turn out to be the telephone companies. LBC received nearly 
70 million calls for one of its reality shows (providing phone-in revenue) from 
22 Arab countries. This is just one such instance.<br>
<br>
<b>Background</b><br>
The emergence of private satellite networks has given the Arab viewers a scope 
to watch a wider variety of channels. The Middle East region saw its first-ever 
private Arab satellite during the Gulf War in 1991, with the launch of the 
Middle East Broadcasting Corporation (MBC). From then on, the audio-visual 
medium has made rapid strides, providing content that ranges from modified 
versions of popular American shows (for instance, Who wants to Be a 
Millionaire?) to discussion forums, and lifestyle programmes.<br>
There are mainly two satellite systems providers that cater to the Arab region, 
Arabsat and Nilesat. Together, the two satellites provide over 150 FTA TV 
channels, more than 20 free radio stations, and carry the TV and radio channels 
of the three main Pay-TV providers in the region -- ART, Orbit and Showtime.<br>
<b><br>
TV &amp; Messaging Boom</b><br>
In the Middle East, interaction between television and messaging is booming, 
thanks to the successfully running TV shows. Future Television, LBC, and Rotana 
have all made use of mobile communications as a feedback channel for their 
content. Channels like Mazzika and Melody split the SMS and call fees with the 
mobile operators with whom they have partnered. Ring tone downloads are also 
providing music producers and distributors with a good revenue source. <br>
<br>
The success story of the reality shows has attracted big companies, who, through 
sponsorships, try to grab their share of pie. For instance, Ford Middle East is 
sponsoring the third season of Future Television's ‘Superstar III’ programme. 
The company gets a wider coverage through these programmes when compared to a 
successfully run ad campaign for a month in a newspaper. And it turns out to be 
inexpensive as well!<br>
<b><br>
Popularity Versus Ad Rates</b><br>
The popularity of the channel is the key factor in deciding the ad rates in the 
industry. However, due to lack of systematic measuring method like Nielsen 
People Meter, accurate ratings and viewership are difficult to get. Agencies 
conducting random telephonic interviews will not produce the same results. 
Hence, broadcasters are unable to provide the advertisers with solid audience 
figures. <br>
However, a Mediaedge report says, “The key markets of the region are Saudi 
Arabia and the UAE, that together has over 75 per cent of the entire viewership. 
In Saudi Arabia, Saudi TV1, the national channel, dominates with 21 per cent of 
the viewership. This is followed by MBC (11 per cent), Al Jazeera (10 per cent), 
LBC (6 per cent) and MBC2 (5.5 per cent). Rotana Cinema, Dubai TV and Al Arabiya 
are the other popular channels that make up for the balance. In the UAE, MBC2 
leads the pack with a 12 per cent share. Dubai TV scores 11.5 per cent, Al 
Jazeera 8.5 per cent, MBC and Al Arabiya 7 per cent each, closely followed by 
Abu Dhabi TV at 6 per cent. Prime time (20:00-1:00 local time) viewing is about 
45-50 per cent of the entire viewing.” <br>
<br>
At one time, reality shows attracted high advertising rates due to their 
popularity among the audience. As per the report from the Arab Advisors Group 
titled, 'TV Advertising Rates in the Arab World', TV advertising rates have 
increased dramatically, and programs such as LBC's Star Academy and 'Miss 
Lebanon' charged ad rate upto US$ 11,000, MBC Fear Factor a rate of US$ 10,000, 
and Future's Superstar a rate of US$ 11,500 for a 30-second commercial spot. <br>
<br>
According to Booz Allen Hamilton analysis, the television market witnessed a 
rapid growth in FTV (free channels) sector in the last five years, which called 
for the attention of the advertisers. Ad revenues have grown by an estimated 
annual average of 20 per cent on a net basis between 1999 and 2004 -- an 
indication of the boom. <br>
<b><br>
</b>
<img border="0" src="tv2.jpg" width="308" height="190" align="left"><b>Rising Ad Spends</b><br>
Advertising spends in the region have increased with a robust compounded annual 
growth rate of 25 per cent since 2000. At the end of 2004, advertising spends 
stood at US$ 3.9 billion. Of that, contribution from television is a little over 
50 per cent. The television medium has registered the fastest growth rate in the 
past few years at compounded annual growth rate of 33 per cent since 2000. 
Significantly, more than 60 per cent of the ad revenue contribution comes from 
six channels only. MBC leads with 19 per cent, MBC2 with 14 per cent. LBC, 
Future TV and Abu Dhabi TV contribute 13 per cent, 10 per cent and 6 per cent 
respectively, the report adds. <br>
<br>
Private FTA channels, including the Big Four of LBC, Future, MBC and Al Jazeera, 
are among the market leaders in advertising revenues. LBC saw an increase of 
nearly 88 per cent in its advertising revenue in the years between 1996 and 
2000. MBC saw a rise of 23 per cent in ad revenue in the same years. Notably, 
neither company backs up the figures.<br>
<br>
A recently released study by the Pan Arab Research Center (PARC) shows that the 
total spending on advertising in the Arab World jumped from US$4.67 billion in 
2004 to US$5.4 billion in 2005, an increase of 17.2 per cent. The same study has 
indicated that the total spending on advertising in the Gulf States rose from 
US$2 billion in 2004 to US$ 2.55 billion in 2005, an impressive 28 per cent 
rise. <br>
<br>
In general, advertising expenditures in the Gulf States during the decade from 
1994 to 2004 grew by 464 per cent. In the UAE, expenditures rose from US$ 632 
million in 2004 to US$ 903 million in 2005, a 43 per cent jump. The UAE has 
surpassed Saudi Arabia to become the largest market for advertising in the Gulf 
region. <br>
<br>
According to a market study quoted by Arab News, the advertising expenditure in 
the pan-Arab media -- mainly the satellite channels -- increased by only 1.14 
per cent. Advertising expenditure in the pan-Arab media had a share of 47.36 per 
cent of the total expenditure in the first quarter of 2005 compared to 38.22 per 
cent in 2004.<br>
<br>
If the trend continues, by 2010, TV advertising revenues across the region are 
expected to be worth more than US$ 3 billion. However, the competition among the 
free-to-air and pay TV channels is pulling down the advertising rates. The Booz 
Allen Hamilton study reveals how networks play against each other to get more 
advertising revenues. They went on to buy airtime rates at heavy discounts — 
some by as much as 95 per cent. Analysts agree that if calculated at the 
official rate, the total value of ads per year on the FTA in the region would be 
US$ 1-2 billion. However, all 155 broadcasters ultimately bring in the combined 
revenue of US$ 200 million, as things now stand.<br>
<b><br>
Mixed Signals</b><br>
Arab Advisors Group’s analysis confirms that TV advertising rates in the Arab 
region have actually dropped considerably since 2004. Judeh Siwady, an Analyst 
at Arab Advisors Senior Research, stated in a report: “When averaged, the 2004 
advertising rates of MBC, MBC2, Future, LBC and Al Jazeera exceed those of 2005 
by around US$ 1,000 for a 30 second TV commercial spot. In 2004 the average of 
the channels’ rates was US$ 3,103 while the year 2005 averaged US$ 2,180, a drop 
close to 30 per cent”.<br>
<br>
With or without the increase of revenues, the television market is experiencing 
a manifold expansion. Both pay TV and FTV channels are struggling to gain ad 
revenues and in maintaining the viewer-ship. For the pay TV channels, which have 
less penetration levels, the task is more difficult. The Pan Arab Research 
Center data shows that in 2000, LBC amassed over five more times advertising 
revenue than did ART, netting US$ 171 million versus ART’s US$ 32 million.<br>
<br>
Pay TV operators have to expand viewership to uphold their subscription 
revenues. At the same time, FTV has to sustain its ratings to get higher ad 
revenues. In the end, it is the programme mix and content that will decide the 
popularity factor and distribution of ad revenues. <i><br>
<br>
- Sowmya Sri Rallabhandi</i></font></p>
