                    Cover Story<br>

                    </font></font>

                    <font face="Verdana" size="4" color="#FF0000">Between a Rock and a Hard Place</font>

                    <p><font face="Verdana" size="1"><b>Pakistan's decision to side with the United States in the war against terrorism has not been without cost. Its economy is suffering, refugees are queuing up at its borders, and there is rioting in its cities. In our special report on Pakistan, OER correspondent Ujala Farooque gauges feeling on the streets of Pakistan</b></font></p>

                    <p><font face="Verdana" size="2">A cartoon in Pakistan's leading English language newspaper 'Dawn' last month depicted an anxious President Pervez Musharraf, arms and hands outstretched to the limit, touching with one finger the tip of Uncle Sam's index, and with the other a finger of a Muslim cleric.&nbsp;<br>
                    It dramatised very effectively General Musharraf's dilemma.<br>
                    His decision to side with the United States in its war against terrorism and the Taliban was always going to alienate a section of Pakistani society. Now, the ferocious US attacks on Afghanistan, combined with Pakistan's fragile economic condition, are stirring the cauldron of discontent.&nbsp;<br>
                    Strikes are being called in Karachi on an almost daily basis by parties opposing US military action in Afghanistan, and are affecting the socio-economic activities in the city. The clearing of cheques by banks, processing of shipping bills for export, and port operations remain disturbed. Financial, corporate and educational institutions are all in a state of uncertainty.<br>
                    Roadblocks erected all over the city to divert traffic away from US and other diplomatic buildings are causing traffic problems and frustration. Even though the major industrial companies are trying to make up for lost work days by operating on weekends, productivity levels are estimated to have dropped by 30 to 40 per cent. Manufacturing output has slumped by up to 50 per cent on some days.&nbsp;<br>
                    Retail and wholesale markets remain closed on days when strikes have been called, for fear of shops being stoned, looted or burned down. Transporters of goods also refrain from operating on such days, causing a shortage of supplies in the city.&nbsp;<br>
                    In the northern region of Pakistan there are reports of hospitals closing for an indefinite period "due to the uncertainty in the area".&nbsp;<br>
                    Multinational companies in the energy sector have informed the Government of their decision to evacuate expatriate employees. Some foreign companies are closing down, others are curtailing their operations.&nbsp;<br>
                    In the textile industry, falling yarn prices and fewer orders have textile millers anticipating closure of their mills. The tourism sector has been decimated.&nbsp;<br>
                    The Government estimates that in total, the chaos caused by the war on terrorism could cost Pakistan as much as US$2.5 billion, depending on how long the military action lasts.<br>
                    While negative economic repercussions are the cause of concern for many Pakistanis, many more find the situation disturbing on moral and religious grounds.<br>
                    Even before the US started bombing Afghanistan, religious parties were calling for nationwide strikes in anticipation of the military action. A strike in Karachi in late September left four people dead and several others injured as protesters clashed with police. The participants of a rally in Faisalabad the same day condemned US aggression against Afghanistan and the Pakistan Government's cooperation in this regard. Speakers threatened civil disobedience if the Government continued to support the US. A resolution adopted on the occasion said if the US attacked Afghanistan, suicide squads would be formed to attack US installations in Pakistan. The resolution proposed a Rsl00,000 (US$1,600) reward for anybody killing a US soldier.<br>
                    Before the bombing there were demonstrations in Afghanistan too. On 19 September, 15,000 Afghanis reportedly participated in a demonstration organised by supporters of the Taliban at Weesh, a trading town on the Pakistan-Afghanistan border. Demonstrators armed with AK-47 assault rifles and rocket-propelled guns chanted "Death for America", and vowed they would not surrender to the US.<br>
                    The violence is getting worse.&nbsp;<br>
                    On October 28, gunmen massacred 18 Christians as they prayed in a church in Bahawalpur, shouting "graveyard of Christians, Pakistan and Afghanistan. This is just a start".<br>
                    Nawabzada Nasrullah Khan of the Pakistan Muslim League says he believes that without the Musharraf Government's cooperation, the US would have found it difficult - perhaps impossible - to attack Afghanistan. He says by joining the anti-Afghanistan coalition, the military rulers of Pakistan have landed the country in a quagmire. He is also critical of Pakistan's collaboration in efforts to overthrow the Taliban, calling it a foreign policy failure.&nbsp;<br>
                    Religious groups like the Jamiat Tulaba-i-Islam (JTI) are more vociferous than ever before. Syed Izzatullah Shah Bukhari, the central convener of JTI, says the entire JTI are willing to lay down their lives for Islam. He says the JTI's support for the Taliban ranges from logistic support and staging protest demonstrations and rallies, to more direct support in the form of volunteers leaving for Afghanistan to take part in Jihad.<br>
                    Meanwhile, the United Nations refugee agency, UNHCR, says more than 60,000 panic-stricken Afghan refugees have crossed over into Pakistan since the US attacks began, and tens of thousands more are queuing at the borders. It warns that in a worst-case scenario, up to 1.5 million refugees could flee Afghanistan.&nbsp;<br>
                    Some prominent personalities, such as Pakistan's noted social worker Abdul Sattar Edhi, have set up relief funds to help Afghanis. Others have spoken out strongly against allowing Afghan refugees into Pakistan. Nisar Ahmed Khuhro, President of the Sindh PPP - a political party that opposes Pakistan's alignment with the US - says the influx of refugees will result in innumerable problems for the country. He has called on Pakistanis to refrain from providing jobs and shelter to the refugees.&nbsp;<br>
                    All of these incidents, alongside the immediate political and economic problems, are contributing to the general public's confusion and angst.&nbsp;<br>
                    General Musharraf's public addresses have eased some of the tension. The General has sought to assure people that what he is doing is for the long-term benefit of Pakistan, which is burdened economically by a mountain of debt.&nbsp;<br>
                    He has spoken of financial aid, the lifting of US sanctions (placed on Pakistan after it conducted nuclear tests in 1998), defence of the nation and, above all, economic prosperity.&nbsp;<br>
                    Javed Bokhari, an economist who works for Dawn newspaper, says that from an economic point of view, the country had little choice but to support the US.<br>
                    "Pakistan could not appear to be isolated in this campaign against terrorism," he says.&nbsp;<br>
                    "But it also has to be with the international community and the US because of its economic condition. We are in a situation because of the mountain of debt that we have accumulated. We need economic assistance to develop our own economy. We need enough foreign exchange reserves to attract foreign investors. In the age of globalisation, we had to do something to be with the international community."&nbsp;<br>
                    Bokhari says that if General Musharraf had refused to assist the US he would have destabilized the economy, and his own hold on power.&nbsp;<br>
                    "Musharraf's stability depends on sustained economic growth. If Pakistan was in a position to mange its economy independently, then Musharraf's siding with the US might have been questionable. But this issue is too big to be judged in terms of individual interest."<br>
                    Others remain unconvinced that the decision to stand with the US is going to help Pakistan's economy.<br>
                    "We are only getting US$3 billion written off in US debts," says economist Akbar Zaidi. "The total debt is US$36 billion. So it is making a very minute difference. The US is not going to write off all the debts. It is a misconception that the removal of sanctions will have any economic impact."</font></p>
