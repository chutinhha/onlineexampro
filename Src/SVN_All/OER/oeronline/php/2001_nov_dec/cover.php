                                  <p><font face="Verdana" size="2">Economically, there two key issues Pakistan and Pakistanis are concerned about:<br>
                                  1. The write off or rescheduling of debt. Pakistan's external debt totals US$36 billion, of which almost $17 billion is borrowed from European countries and the US. If the bulk of this is written off and the rest is rescheduled, that would be a significant boost to the economy.&nbsp;<br>
                                  2. Opening up of US and European markets to Pakistani exports. At present Pakistani exports to the US are worth about US$5 billion. If the US allowed this to double, Pakistan would benefit significantly but it would make a very negligible difference to the US itself. Therefore, Pakistan is pressing for increased export volumes.</font></td>
