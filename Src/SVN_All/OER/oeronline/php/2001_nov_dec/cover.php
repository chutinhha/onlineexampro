                    <p><font face="Verdana" size="2">Most Pakistanis agree that refusing to support the US was not really an option for Pakistan politically. But it will take more help from the US on the economic front for people to feel comfortable with General Musharraf's decision.&nbsp;<br>
                    Pakistan's ties with the US and the West are much more important than the goodwill of the Taliban regime. But the US must appreciate that by supporting its offensive against Afghanistan, the Pakistan Government is paying a hefty price.&nbsp;<br>
                    Pakistan's Afghan policy seems to be in a shambles. The anti-Taliban Northern Alliance was already displeased with Pakistan, and now the Taliban regime has become an enemy.&nbsp;<br>
                    Some say that while it had the chance, Pakistan should have used its influence on the Taliban to moderate its policies. The Government says it had "no influence" on the regime in Afghanistan, but some people remain unconvinced.&nbsp;<br>
                    The Taliban are heavily dependant on Pakistan for food, transport, and access to the outside world. Some commentators argue it was within Pakistan's power to do some hard arm-twisting. Had Pakistan forced Osama bin Laden out of Afghanistan six months ago, they ask, would it be facing such a dilemma now?</font></p>

