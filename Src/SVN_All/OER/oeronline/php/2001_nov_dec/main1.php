                  <td width="100%"><font face="Verdana" size="4" color="#FF0000">Standing Tall</font>

                    <p><font face="Verdana" size="1"><b>Pakistan's Ambassador to Oman, His Excellency Karam Elahi, says his country will weather the current crisis and will not waver in its stand against terrorism. But in an exclusive interview with OER, he tells Matthew Brockett that the United States should also address the causes of extremism</b></font><font face="Verdana" size="2"><br>

                    <br>

                    <img border="0" src="../../images/2001_nov_dec/im4.jpg" align="left" hspace="5" vspace="5" width="225" height="259"><b>OER:</b> Pakistan has denounced the terrorist attacks on the United States. Could you please re-state Pakistan's position on the attacks for the benefit of our readers.<br>
                    Ambassador Elahi: The people of Pakistan, like the rest of the civilized world, were shocked over the enormity of the misery inflicted and the loss of innocent lives. Such transgressions are against the spirit of Islam and cannot be supported under any circumstances. Pakistan itself has been subjected to terrorism over the past 15 years. Even our religious gatherings have been targeted. It is because of this that the Government of Pakistan took no time in denouncing the attacks and announcing its support for international efforts against terrorism.&nbsp;<br>
                    <br>
                    <b>OER:</b> But Pakistan's decision has caused internal friction. Was it a hard decision to side with the United States?<br>
                    Ambassador Elahi: As General Musharraf said, we had to choose the lesser of two evils. It was not an easy decision, but the right decision was taken.<br>
                    <br>
                    <b>OER: </b> The majority of Pakistanis appear to back the Government's decision to join the United States in its fight against terrorism, but a minority have voiced anti-American sentiment and support for the
                    Taliban. How will Pakistan deal with the differing views among its people on this issue?&nbsp;<br>
                    Ambassador Elahi: All major political parties have supported the Government on this issue. The difference of opinion remains manageable.<br>
                    <br>
                    <b>OER: </b> So you don't think the unrest will worsen and spread?<br>
                    Ambassador Elahi: No, I don't think it will spread. Primarily, it's the extreme element (demonstrating). They are members of the religious parties and
                    sympathisers. There are people in Pakistan who feel that they have a lot in common with the people of Afghanistan. The obvious links are ethnicity and a common religion. There are also many other factors. After all, we are
                    neighbours. We do not deny the existence of such elements in Pakistan, but they are in a minority. I think what the Government of Pakistan is doing is to let the steam out. If the people have a difference of opinion, let them say 'we don't agree', because if you clamp down in a big way, then it can simmer. The Government of Pakistan will not use force to stop them from doing this as long as they do not violate the law. If they start burning or if they start fighting or if there is violence of if there is destruction of property, then naturally it is the duty of the Government (to stop it).<br>
                    <br>
                    <b>OER: </b> What effect is the war having on Pakistan's economy?<br>
                    Ambassador Elahi: The immediate result of this has been plenty of disruption. There is a reduction in air movement. Some of the export orders that were being processed have been delayed, put on hold or cancelled. So the immediate impact has been negative for our exports. The second fallout that we are afraid of is a massive further influx of refugees. I do realise that the international community is looking at the possibility of sending in aid. But at the same time there are things that cannot be bought with money. For example, if the refugees move in with their livestock, and they start using the same grazing grounds as our own people, then this is not something that can be compensated through aid.<br>
                    Thirdly of course, you are seeing demonstrations within Pakistan.&nbsp;<br>
                    <br>
                    <b>OER: </b> What is the economic outlook?<br>
                    Ambassador Elahi: We are looking forward hopefully in the medium term and long term to accelerated economic activity in the region. And of course some of the measures that have been announced, with regard to the removal of sanctions on Pakistan and the easing of loans, although most of it was in the pipeline already, will help. If the West does play the role that they have promised to play, then I think things will take a turn for the better. If the economic situation in the region improves, then I think a lot of the causes that create this kind of convulsion and commotion will also go. That will be good for the region as well as for the world. We do hope that in the medium and long term the international community will look at giving a helping hand in rebuilding Afghanistan.&nbsp;<br>
                    <br>
                    <b>OER:</b> Will Pakistan take part in any military action against Afghanistan?&nbsp;<br>
                    Ambassador Elahi: No. We will not. Pakistan has made it clear that its armed forces will not engage in military action against Afghanistan. We have serious disagreement with the government in Kabul on this issue but our deep-rooted relations with the people of Afghanistan preclude the possibility of Pakistan joining in a military attack on them.<br>
                    <br>
                    <b>OER: </b> Do you believe there is an opportunity in this situation for Pakistan and India to improve their relationship?<br>
                    Ambassador Elahi: Definitely. However, unfortunately India appears to look at the events of 11 September as an opportunity to push its own unilateral interests. You only have to look at its leadership's statements and its media's reports - often drastically convoluted and tendentious - to understand what I mean.&nbsp;<br>
                    <br>
                    <b>OER:</b> Do you hold any concerns that US military action against the Taliban will precipitate a wider conflict between Muslims and the West?<br>
                    Ambassador Elahi: We have to do all we can to ensure that all sides look at the developments as they really are, that is, efforts to deal with terrorism. I hope that sometime down the road attention will also be paid to the causes that breed extremism.<br>
                    <br>
                    <b>OER: </b> Does Pakistan feel the United States should review its policies in the Middle East, particularly with regard to Israel and Palestine?<br>
                    Ambassador Elahi: Pakistan has been one of the most steadfast supporters of the inalienable rights of the Palestinian people. We believe there can be no genuine peace till these rights are fully respected. We have always said this to the whole world, including the United States.<br>
                    <br>
                    <b>OER:</b> Do you think there is a general distrust of, and resentment towards the United States in the Middle East that makes it more difficult for people here to sympathise with the war on terrorism?<br>
                    Ambassador Elahi: I would say that people think the United States has not been supportive of their causes. I don't think that anyone expects the United States to be supportive of the Palestinian cause.&nbsp;<br>
                    But generally, the people feel that the United States has not been even handed. The way we look at it is that, at this moment, the first target should be terrorism, but down the road, later on, it is necessary that some of the concerns of those people who feel that they have been neglected should also be addressed.<br>
                    This can take many forms. Apart from the question of self-determination, there are other things like extreme poverty. Even in the West there are have been demonstrations about economic disparities. So we hope that eventually, once we are through with the first phase, we will also move to addressing some of the problems.<br>
                    General Musharraf has been quite candid in explaining this. He said if you hit terrorism without going to the roots of the problems, then actually you will be shedding the leaves of the tree, the tree will still be there.<br>
                    For the moment, naturally, this is a monster which has just emerged and you have to first deal with it.&nbsp;<br>
                    But then attend to some of the problems which are creating this kind of mindset, at least in some sections of people.<br>
                    <br>
                    <b>OER:</b> Do the events of September 11 signal a significant change in the world order? If so, could you explain how you see things changing?<br>
                    Ambassador Elahi: Even though at the moment major concern seems to be the fight against terrorism, efforts will have to be made to bring about a little more justice, a little more even-handedness in international relations.&nbsp;<br>
                    There is a need to address the root causes of extremism and to rectify injustices, repression and humiliation.<br>
                    We are in the eye of the storm at the moment, and we are a front-line state once again. We will play our role.<br>
                    <br>
                    <b>OER: </b> Thank you</font></p>

