                  <td width="100%"><font face="Verdana" size="4" color="#FF0000">Shipshape</font><p><font face="Verdana" size="1"><b>Salalah Port Services (SPS) is gearing up for the arrival of the next generation of container ships, due to hit the high seas later this year. Chief Executive Jack Helton spoke exclusively to Matthew Brockett about a US$200 million development plan to increase the size of Salalah's harbour and develop the port in anticipation of the new mega vessels&nbsp;</b></font><font face="Verdana" size="2"><br>

                    </font><font face="Verdana" size="2">

                    <br>

                    <img border="0" src="../../images/2001_nov_dec/im6.jpg" align="left" hspace="5" vspace="5" width="225" height="149"></font><font face="Verdana" size="2">Salalah is already one of only six ports in the world that can handle "S-class" vessels - the biggest container ships in service. These vessels carry a standard load of 6,500 twenty foot equivalent (teu) containers, and are 17 containers wide.&nbsp;<br>
                    "In 1999, this class of vessel represented five per cent of the world container shipping capacity," says SPS Chief Executive Jack Helton.&nbsp;<br>
                    "By the end of this year, it's going to be getting close to 30 per cent. In two to three years, these ships are going to be common. But at the moment they can only go into certain ports."<br>
                    No wonder, then, that many ports around the world are rushing to expand their capacity so that they can service these big ships and get a piece of the pie.&nbsp;<br>
                    But SPS wants to stay a step ahead of the pack. The port has already started to equip itself to handle the next generation of super container ships - floating mountains that, at an enormous 450 metres long, will be 30 per cent bigger than the S-class ships.<br>
                    "They are 10,000 to 12,000 teu vessels," Mr Helton says. "They're going to be a new class of vessel. We don't know what they're going to be called, but they'll be so big that at least 90 per cent of the ports in the world won't be able to work them."&nbsp;</font></p>

                    <p><font face="Verdana" size="2"><img border="0" src="../../images/2001_nov_dec/im9.jpg" align="left" hspace="5" vspace="5" width="225" height="154">The new ships will be 22 containers wide, and stand a lot higher in port than the S-class vessels. SPS has already purchased five new "super" cranes that are five metres taller than standard cranes in anticipation of the mega ships.<br>
                    "We were the first one to get out on a limb and do that," Mr Helton says. "Obviously that's hurt our earnings a little bit this year. But we are now in a position today to work the next generation of vessels. Every port in this region is just trying to catch up to where we are today. We're already prepared for the next one."&nbsp;<br>
                    But the expansion plans go much further than that.&nbsp;<br>
                    SPS has a current annual capacity of 1.8 million teu, and will achieve 1.3 million teu this year. But based on a conservative estimate by an independent consultant, the port will outgrow its current capacity by 2004.<br>
                    "We would like to think that we'll be out of capacity by the end of 2002, so it's time for us to start looking ahead and planning for the future," Mr Helton says.<br>
                    "So we've recommended to the Government that we expand the existing breakwater, which will increase the capacity of the harbour by 25 per cent. It will position the port for future growth for the next 50 to 100 years."<br>
                    The US$200 million development plan includes deepening the harbour from 16 metres to 18 metres, expanding the container terminal and increasing the number of berths.<br>
                    Mr Helton says the expansion will allow SPS to develop a terminal for cruise ships, which are visiting Salalah in increasing numbers (at present, about 30 passenger cruise ships visit Salalah every year). But more importantly, it will ensure that the next generation of mega container ships have every reason to use Salalah as a port.<br>
                    "We want to make sure that there are no reasons for any of these guys to avoid using Salalah," Mr Helton says.&nbsp;</font></p>

                    <p><font face="Verdana" size="2"><img border="0" src="../../images/2001_nov_dec/im5.jpg" align="right" hspace="5" vspace="5" width="250" height="168">"When we expand, we will dredge (the harbour) so that we can handle these vessels easily, and we will have a turning basin."<br>
                    The company hopes to start work on the expansion by the middle of this year and have it completed by 2004.<br>
                    "We're going to the Government, recommending that this project gets underway as soon as possible next year so that we're prepared for the next generation of ships, which we anticipate will hit the market late next year," Mr Helton says.<br>
                    "We're getting a big influx of the S-class type of vessel now. Almost everybody is upgrading to this class. But there's at least three companies that I'm aware of that are going beyond this now to the bigger ships."&nbsp;<br>
                    SPS does not expect the expansion to result in an immediate lift in volumes, but Mr Helton says it will help Salalah to establish itself as a "mega hub" in the future.<br>
                    "It does make us more marketable. We have the location, we have the resources, and we have the productivity, so it makes it a lot easier when you're out marketing. This should become a mega hub."<br>
                    Aiding the transformation of Salalah Port into a mega hub is the Salalah Free Zone, scheduled to open for business this year. The Free Zone master plan calls for an entire community to be developed in multiple phases over an area of 2,400 hectares (6,000 acres) next to the port.<br>
                    It will offer a mix of industrial, light manufacturing, warehouse, logistics, distribution, research and development and office facilities, as well as retail outlets, a hotel, a golf course, a resort and residential housing.<br>
                    Phase One, which will provide customers with 400,000 square metres of distribution, logistics, freight forwarding and manufacturing facilities, will be completed in the first quarter of 2002 at an estimated cost of US$80 million.&nbsp;<br>
                    The Salalah Free Zone Company that will manage the Free Zone under a 99-year lease concession agreement is a joint venture between the Government of Oman (40 per cent), US company Hillwood (40 per cent), and Salalah Port Services (20 per cent).<br>
                    Mr Helton says Salalah Free Zone and Salalah Port will compliment each other to create a thriving import/export hub - an important engine for economic growth as Oman looks for new sources of income away from oil and gas.<br>
                    "It's purely achievable to create 20,000 to 25,000 jobs between the port and the free zone in the next 20 years," he says. "Think about that in terms of the percentage of this country. If you take the trickle-down impact of that, all of a sudden you've got a very serious critical mass that makes a serious contribution to this country and is not dependent whatsoever on energy.<br>
                    "And that's why we're promoting increasing the size of the harbour, because we've completely dispelled all the myths."<br>
                    The first myth, he says, was that the port would not be able to work ships during the Khareef (monsoon) season.&nbsp;<br>
                    "Our competitors said: 'you'll have 10 weeks out of the year that you can't work cargo ships down there because of the monsoon weather'.&nbsp;<br>
                    "Well, we've operated now through three monsoon seasons, and we've set world production records."&nbsp;<br>
                    The second myth was that with 55 to 60 per cent of its workforce Omani, the port would struggle to operate effectively during Ramadan.<br>
                    "We set two world production records, one in Ramadan," Mr Helton says.&nbsp;<br>
                    "So what I've said to the Government is: 'we've proven the productivity is there; nobody challenges that any more. The location is there. So the only thing that will restrict our growth in the future is our inability to have a big enough vision'."<br>
                    <br>
                    SPS is jointly owned by Maersk Sealand (30 per cent), the Government (20 per cent), private investors (19 per cent), pension funds (11 per cent) and the investing public (20 per cent). SPS made a net profit of RO932,000 in the six months to June 30 this year, and expects to post a full-year net profit of RO1.5 million - down slightly from the RO1.9 million achieved in 2000.</font></p>

