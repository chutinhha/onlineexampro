                  <td width="100%"><font face="Verdana" size="4" color="#FF0000">The champion of e-shopping!</font><p><font face="Verdana" size="1"><b>Originally started as an online book retailing micro enterprise way back in 1994, Amazon.com today has become the world’s most dynamic and biggest store for buying and selling virtually anything and everything. OER reports</b></font><font face="Verdana" size="2"><br>
                    <br>
                    <img border="0" src="../../images/2002_april/p-29.jpg" align="left" hspace="5" vspace="5" width="123" height="238"></font><font face="Verdana" size="2">On a May day in 1994, a 30-year old man was sitting at the computer in his 39th floor office in midtown Manhattan, exploring the still immature Internet, and he found a site that purported to measure Net usage. The man couldn’t believe it! The Internet was growing at a rate of 2,300 per cent a year. It was a kind of wake-up call for him.<br>
                    Eight years later he is the proud founder and CEO of Amazon.com, earth’s biggest online store, with record breaking net sales of US$3.12 billion for the 2001 fiscal year and employing over 7,800 people worldwide. Yes, we are talking about Jeff Bezos who left his cushy Wall Street job at DE Shaw and Co. to start Amazon.com out of his garage.&nbsp;<br>
                    In 1994, while studying retailing opportunities on the Internet, he decided on books because there was a broad field of book publishers but too many titles to be carried by a single store. The characteristics of the books retailing industry make it amenable to electronic commerce: a great variety of products and consumer tastes, and tastes which hanker after a lot of information about the products. Moreover, there is room for bringing down margins, i.e. offering the customers deep discounts.&nbsp;<br>
                    Jeff Bezos picked the name Amazon because it is the biggest river on earth. He wanted his online bookstore to become “earth’s biggest bookstore.’ But, what started as earth’s biggest bookstore is rapidly becoming world’s largest all-purpose store. Amazon.com, a pure-play consumer e-commerce company, offers more than 18 million unique products: books, music, videos, toys, electronics, software, home improvement products, auctions and online greeting cards...<br>
                    Amazon.com has enjoyed rapid expansion in all aspects of its operations, including business turnover and a spectacular rise in share value since public floatation in 1997. It sells only online and is essentially an information broker. It holds a relatively small, though increasing inventory and out sources most aspects of its operations (but not IT). The key to its operation is to offer value added and sophisticated customised services, a continuously expanding catalogue of products in terms of both quantity and range, and deep discounts.<br>
                    <br>
                    <b>The business model</b><br>
                    Amazon has managed to pioneer a powerful new business model for e-commerce, one that could potentially be applied to many different consumer products. It has transformed the book trade from being a commodity-based, supplier-driven industry into a service-based book-buying community - a community which gives a greater influence not just to the book-buying public but also the independent publishers and authors.&nbsp;<br>
                    Amazon.com did more than just selling books; it offered a unique, customised service: information about books that are of relevance to the individual potential customers. These include synopses, excerpts, and reviews including reviews submitted by readers. The portal has built-up an extensive community of buyers through positive feedback. In addition, it accumulated a substantial database of customers’ preferences and buying patterns, tied to their e-mail and postal addresses. Such information has enabled the company to build-up a large community of loyal customers, generating an impressive “repeat orders’ rate of over 70 per cent.<br>
                    The business model has also revolutionised the supply side of the market. Amazon.com has pioneered syndicate selling on the web - websites passing clients onto each other by establishing and taking a commission on sales. Secondly, the business model has broken the principle of critical mass for the book market. For the first time, small and independent publishers as well as authors could place their products directly in a (online) store with global reach and without investments (except paying for transporting books to Amazon.com’s distribution centre).&nbsp;<br>
                    As Jeff Bezos stated, “Ultimately, we’re an information broker. On the left side, we have lots of products and on the right side, we have lots of customers. We’re in the middle making the connections. The consequence is that we have two sets of customers: consumers looking for books and publishers looking for consumers. Readers find books or books find readers.”&nbsp;<br>
                    He has applied the same model to other products also such as CD’s, videos, toys etc. Some of the expansions away from its core (books) have been highly successful, namely music and video, while others still fail to make a significant impact (greeting cards and auctions for instance). Either way, Amazon.com continues to add new retail categories to its offer and is well underway to achieve its goal of becoming a place where anyone can buy anything anytime one wishes.&nbsp;<br>
                    <br>
                    <b>Brand marketing</b><br>
                    Amazon.com markets via high visibility online presence. Despite a relatively short corporate history, Amazon.com has built a powerful brand name. Today, whoever wants to compete with Amazon finds it difficult and prohibitively expensive to build a similar level of brand awareness and shopping traffic.<br>
                    The Amazon.com brand name derives its popularity from two areas:&nbsp;<br>
                    nA large product selection complemented with additional information about these products.&nbsp;<br>
                    n A pricing policy which offers considerable discounts for many of its products.&nbsp;<br>
                    Online retailing is a low-margin affair and the ease with which comparison-shopping can be done on the Internet will always keep prices low. In a low-margin business, the companies that ultimately succeed are those that can keep operating costs low. Amazon, being the largest, believes in building efficiencies of scale. This means not only cost savings in terms of its massive bulk buying from suppliers but also in terms of the fast and efficient distribution system Amazon has set-up. “Our improvements in productivity allowed us to lower book prices and now allow us to offer free shipping,” says Warren Jenson, Chief Financial Officer, Amazon. “We expect that these money-saving initiatives for customers will continue to play an important role in our future growth.”<br>
                    <br>
                    <b>Selling</b><br>
                    Amazon.com only sells online via its websites. The company currently operates four international websites for selling: www.Amazon.co.uk, www.Amazon.de, www.Amazon.fr and www.Amazon.co.jp. It offers a comprehensive product cataloguing system, integrated with a range of searching mechanisms, which enable customers to select products with precision. Selling is enhanced by additional product information from various sources. Matching services between customers and products (in accordance with customer preferences) are also available as are a series of recommendation services.&nbsp;<br>
                    Ordering is via a simple shopping cart. Customers are given a choice of secure or standard servers. Order confirmation is sent to customers via e-mail. Amazon.com automatically opens an account for a customer placing his first order. Future ordering by a customer is simplified by “1-Click” (TM) express shopping, which automatically invokes all the relevant details of the customer.<br>
                    Customers pay by using credit card, cheque, money order (denominated in the currency of the country of the website), or gift certificates. Amazon.com does not accept purchase orders but charges the customer’s credit card when an order is shipped. On the other side, whenever an order has been placed via cheque or postal order, shipment is released after clearance of funds.&nbsp;<br>
                    It may be interesting to note that the company actually has a negative cash conversion cycle. This means that the company is able to collect its sales dollars from customers before it pays its distributors and vendors!<br>
                    In spite of being a trendsetter in the e-business marked by such an impressive performance, Amazon.com still suffers from accumulated deficits of US$2.86 billion since the start of business. But Jeff Bezos is as committed and positive about Amazon.com as he was eight years back when he says, “We have a tonne of doubters, and the fact of the matter is, we don’t try to convince them” pointing out that he will start making profits when the “cone of opportunity’ begins to narrow - that is when there’s no room left for more competitors to enter!&nbsp;</font></p>
