                    <font face="Verdana" size="4" color="#FF0000">Tying the knot&nbsp;</font>
                    <p><font face="Verdana" size="1"><b>It may not be a marriage of equals but Bank Dhofar and Majan International Bank are hopeful of a win-win situation. Is there really room for more than one clear victor from such an alliance? Nayl D’Souza reports&nbsp;</b></font></p>
                    <p><font face="Verdana" size="2">A few weeks ago, the talk
                    in banking circles spiralled along an axis of “will they
                    or won’t they?”. At the time of OER going to press, the
                    final verdict from the jury (ultimately, the shareholders)
                    is still to be cast but insiders at the banks privately
                    forecast that the results from the due diligence of each
                    bank will probably give two synchronised green lights for
                    the merger to proceed. The consolidation could be given the
                    final go-ahead before the end of April and the merger be
                    completed well before the end of the year if both sets of
                    shareholders agree with the analyses conducted by KPMG (on
                    behalf of Bank Dhofar Al-Omani Al-Fransi SAOG) and Ernst and
                    Young (on behalf of Majan International Bank SAOC). One key
                    Bank Dhofar official glibly predicts: “There is no reason
                    for there to be any negative feelings during or after the
                    presentation of these findings.”&nbsp;<br>
                    Wracked by provisions and assailed by non-performing loans
                    in the last few years, local banks remain open to the idea
                    of mergers because they can provide better economies of
                    scale, spread expenses on essential resources and provide
                    wider distribution channels, particularly branches. The
                    continuing jitters in the MSM and global markets, volatile
                    oil prices and the ramifications of 9/11 are also factors
                    which are stepping up pressure on local banks to consolidate
                    during stagnant times.<br>
                    “The difficult market environment has raised the
                    importance of this merger which I personally think could
                    strengthen Bank Dhofar while protecting Majan Bank
                    customers,” says Khalfan Rashid Al Tal’ey, deputy
                    general manager of Oman International Bank SAOG.<br>
                    Yet some commentators in the local media have likened this
                    banking story to a damsel in distress being rescued by a
                    knight in shining armour. Majan International Bank (MIB)
                    strongly contests the view that banks would contemplate
                    merging for charitable reasons.&nbsp;<br>
                    “We’re not in need of rescue. There are very few banks
                    in the local market with a 19.6 per cent capital adequacy
                    ratio and we’ve made sure that all the bad assets have
                    been fully provided for,” says Salaam Al-Shaksy, MIB’s
                    general manager. Capital adequacy measures the ability to
                    convert investment into cash so it is a useful measure of
                    liquidity. The Central Bank of Oman prescribes a capital
                    adequacy of at least 12 per cent.<br>
                    “What interests me [from the merger] is how we can
                    significantly reduce interest rate expenses as well as
                    boosting our small branch network. Presently, we can only
                    open two or three branches a year and we face regulatory
                    constraints in banked areas and so there are hurdles to
                    overcome. The non-branch solution for expansion is to build
                    up our IT, but this proposition is both expensive and
                    difficult to sell to our customers in the interior [who
                    prefer personal interaction]. By merging, we can achieve
                    better reach overnight and size helps. The combined balance
                    sheets of both banks can help boost our capabilities in
                    project financing and corporate lending,” Mr Al-Shaksy
                    adds.<br>
                    Bank Dhofar has grown its branch network to 43 and ATM
                    network to 57. Customer deposits in the bank rose by 27 per
                    cent to reach RO268 million compared to RO211 million at the
                    end of 2000. Meanwhile, Bank Dhofar’s total assets in 2001
                    leapt 24 per cent to RO337 million from RO273 million at the
                    end of 2000 and it announced a RO 6.1 million profit for
                    fiscal 2001. Majan International Bank has six branches. The
                    bank is likely to post a net loss in 2001. After cleaning up
                    the balance sheet, the asset size of MIB is currently around
                    RO130 million, approximately the same level at the end of
                    2000. The bank grew its assets over 45 per cent in 1999.<br>
                    The merged entity will have greater assets than Oman Arab
                    Bank (OAB) which has RO320 million in 2001. The bank’s
                    chief executive officer Abdul Kader Askalan told OER that
                    the merger would not impact on the steady performance of OAB
                    but general economics would.&nbsp;<br>
                    “I believe that 2002 will be as challenging and difficult
                    as 2001. The Oman economy is likely to be impacted with the
                    forecasted contraction in the world economy following the
                    9/11 incidents in the US. However, it is my belief that our
                    culture of business conservatism and our unwavering focus on
                    customer service will see us through unscathed.” OAB
                    announced a net operating profit of RO6.9 million in 2001,
                    and making a provision of 82 per cent for doubtful loans.<br>
                    Indeed, one analyst believed that while Bank Dhofar has
                    restrained from flexing its muscles when talking about
                    consolidation, “the merger was really an acquisition where
                    one large bank would eat the smaller one for its good assets
                    and then spit out the remaining bad bits.” This is
                    progress, he exclaimed. “Afterall, Oman’s banking sector
                    is getting more fiercely-competitive.”&nbsp;<br>
                    Neither bank seems very impressed with each other’s
                    technological backbone. Indeed, Ali Redha, the former
                    chairman of MIB,<br>
                    highlighted in the bank’s last annual report that the bank
                    was looking at the leverage of its delivery channels in IT
                    in fiscal 2000.&nbsp;<br>
                    <br>
                    <img border="0" src="../../images/2002_april/pg-21.jpg" align="right" hspace="5" vspace="5" width="225" height="175">“OK
                    the competition will increase because the merged bank will
                    be bigger. It will also have more delivery channels and the
                    most important one is the branch network. With a higher
                    capital expenditure, [the merged bank] could open more
                    branches and centre on technology and also improve its ATMs,
                    Internet banking and SMS messaging, and so on. The bigger
                    the bank, the better your costs can be distributed evenly.
                    Both corporate and retail banking could become more
                    competitive,” notes Sunder George, Bank Muscat’s deputy
                    chief executive.&nbsp;<br>
                    Increased competition is welcome. It will keep all the banks
                    on their toes and prevent complacency. The banks will need
                    to give better service, offer better products and be more
                    efficient to meet any future competition from banks outside
                    Oman which may enter once Oman opens its doors as per WTO
                    agreements.<br>
                    When asked about the social cost involved in such an
                    alliance taking place, one local banker explained that the
                    expatriates would bare the brunt of any streamlining in the
                    interim period. During the transition, locals would be safe
                    from unemployment but their levels of salary could be at
                    risk.<br>
                    A senior official close to Bank Dhofar’s board of
                    directors said that the only way that the alliance between
                    the two local banks would be foiled would be “if Bank
                    Dhofar were to give a very shoddy bid.” A high-ranked
                    source at Majan identified the possibility of the deal going
                    sour if future negotiations reached an agonisingly slow or
                    over-cautious stage.&nbsp;<br>
                    Analysts say that the shareholders of MIB may not be willing
                    to wait for a long time before receiving cash dividends and
                    as a new bank it has struggled to cope with an increasingly
                    competitive market. The question of how much the merger will
                    cost Bank Dhofar is also unclear. Not many analysts are
                    willing to put a price on such a tie-up.&nbsp;<br>
                    The earnings per share of MIB is thought to be at least
                    three times than that of Bank Dhofar even after provisioning
                    but most bankers do not expect MIB to be bought
                    cheaply.&nbsp;</font></p>
