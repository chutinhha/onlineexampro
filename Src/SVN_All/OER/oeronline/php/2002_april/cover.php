                  <td width="100%"><img border="0" src="../../images/2002_april/pg-20.jpg" align="left" hspace="5" vspace="5" width="225" height="186"><font face="Verdana" size="4" color="#FF0000"><font face="Verdana" size="2" color="#C0C0C0">::
