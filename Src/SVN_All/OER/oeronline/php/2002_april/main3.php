                  <td width="100%"><font face="Verdana" size="4" color="#FF0000">Economic integration, a must for Gulf countries&nbsp;</font><p><img border="0" src="../../images/2002_april/p-38.jpg" align="left" hspace="5" vspace="5" width="225" height="169"><font face="Verdana" size="1"><b>The Gulf nations have been depending on oil as their primary source of revenue generation for too long now. With the fluctuation in international oil prices, it has become imperative for them to open up to the idea of a free market economy. Saleh al-Shaibany reports</b></font><font face="Verdana" size="2"><br>
                    <br>
                    </font><font face="Verdana" size="2">The Gulf countries, accustomed to strong returns from oil revenues, are now coming to terms, with the fact that income from the hydrocarbons cannot sustain their economies in the long run. The fluctuating international oil prices have predominantly left the Gulf states in a budget deficit dilemma. Therefore, it is necessary for them to speed up industrialisation.&nbsp;<br>
                    The six nations of the Gulf Cooperation Council (GCC) have projected a combined budget deficit of US$21.32 billion in 2002, compared with US$8.54 billion in 2001, an astonishing increase of nearly 150 per cent. The GCC economic and political alliance, formed in 1981, comprises Oman, Saudi Arabia, Kuwait, United Arab Emirates, Bahrain and Qatar.<br>
                    The Gulf countries, over the years, have consistently delayed launching economic reforms, preferring to ride out lean periods of low oil prices until their recovery puts them back on track. The International Monetary Fund (IMF), in a recent report, told the Gulf Arab states to introduce taxes and cut down on subsidies to increase non-oil revenues. The IMF also urged Gulf states to attract more foreign funds in order to diversify away from oil and create jobs for their increasing population.&nbsp;<br>
                    “The biggest challenge for the GCC will be committing to the ideal of a free market economy. There will be an adjustment, there will be pain and we have to forge our way through it,” Bahraini Crown Prince Sheikh Salman bin Hamad al-Khalifa told GCC officials in an economic conference in Saudi Arabia last January.<br>
                    High expenditures and heavy dependence on oil revenues have slowed down the GCC economies but economists have warned that the gulf states, which sit on 45 per cent of the world’s proven crude oil reserves, must speed up reforms.&nbsp;</font></p>
                    <p><font face="Verdana" size="2">“Reforms mean putting oil money into projects and opening up to international investors with little delays,” said Robert Lees, a senior executive with KPMG, a consultant organisation.<br>
                    “Privatisation is the key to reforms, as well. Many Gulf nations are still dragging their feet when it comes to hand over their resources,” he added.<br>
                    With the exception of Oman, which is leading the region with a well-placed privatisation programme, the rest of the GCC countries are yet to catch up. Oman has successfully managed to attract private investments worth US$1.3 billion to build four power plants. It has also handed over the management of its two airports to private investors and negotiations are in an advance stage to sell off the telecommunications company, Omantel.&nbsp;<br>
                    The region’s vast resource base has helped in building a modern infrastructure and has attracted millions of foreign workers, but experts say that this is not enough. “These countries are blessed with oil but the money must also be used to diversify from the energy sector,” former UN secretary general, Boutros Boutros Ghali said in his speech during the World Intellectual Property Organisation (WIPO) conference held recently in Muscat.&nbsp;<br>
                    <br>
                    <img border="0" src="../../images/2002_april/p-39.jpg" align="right" hspace="5" vspace="5" width="173" height="248">The GCC spent a staggering US$95 billion in 2001 mainly on infrastructure, defence and civil service salaries. Officials urge that, in order to push forward reforms, GCC must integrate through joint trades and stop competing with each other. “How can we compete with international economies if we get into each others’ way in our individual trades? For example, there are no efforts of cooperation in our free trade zones or industries like aluminium and natural liquefied gas (LNG),” a highly placed Omani government official said.<br>
                    The European Union (EU) set up various trade barriers for GCC exports like aluminium saying that the Gulf countries must first reduce their custom tariffs. Gulf officials have accused the EU of stalling its decision on free trade, saying the GCC had done everything to reach an agreement consolidating trade worth about US$46 billion. The Gulf, stung by criticism and eager to push trade reforms forward, agreed in the last GCC summit, to advance the date for implementing the customs union to January 1, 2003. The decision lowered the tariffs on foreign imports to five per cent from the previous levies of up to 15 per cent.<br>
                    It is a major breakthrough and the unified customs tariffs is widely expected to pave the way for a free trade pact with the European Union (EU), the region’s main trading partner. However, three months after the December 2002 GCC summit, Gulf officials are still negotiating with the EU.<br>
                    “We have played out part but we are still waiting for them to lift the economic barriers erected against us,” an Omani trade official said. The main dispute is over a six per cent EU duty on primary aluminium Gulf exports. Two aluminium smelters in Bahrain and the UAE produce more than a million tonnes per year, about five per cent of the world’s total. Oman is soon expected to set up its own aluminium plant with a production capacity planned for 450,000 mtpa.<br>
                    However, analysts said that the GCC’s economic challenge would not rest on the customs union but it would be a much larger approach. “The single currency is not going to materialise unless they start talking about setting up one central bank to speed up the process,” a senior banker at BankMuscat said. The Gulf states have decided to establish a single currency by 2010. “This target is too far. Why should it not be sooner?” the banker said.&nbsp;</font></p>
