            buy your electronics from - Japan or Russia? Would you rather invest 
            in Dubai or Dhaka? Countries are brands, and perceptions of a 
            national brand can have a major impact on consumers and investors.<br>
            <br>
            <b>National branding defined</b><br>
            National branding can be defined as (1) establishing an image 
            (internally or externally) for a country based on positive national 
            values and perceptions and (2) emphasising this image when promoting 
            exports, tourism and inward investment (Source: International Trade 
            Centre).<br>
            In recent years, globalisation of trade has meant that products and 
            services can be purchased from an almost limitless number of 
            sources. Differences in price and quality are being steadily eroded. 
            Thus, country origin has becomes a potentially important factor in 
            distinguishing otherwise fairly similar products and services.<br>
            At the same time, with improved access to the Internet, satellite TV 
            and mobile phones, the role of the media has increased enormously. 
            The images created by the media play a tremendous role in shaping 
            our views about places and products. What we think about Japanese, 
            Omani or Chilean products or the investment opportunities they offer 
            is inextricably tied to what we think about that country, and the 
            images that spring to mind are in a large part nurtured by the 
            media.<br>
            In light of this new environment, a number of countries have 
            embarked on media campaigns, often linked with their tourism 
            industry, but increasingly focused on promoting exports and 
            attracting investments. Most such programmes have failed to live up 
            to their promise. This is because, although capitalising on the 
            promise of media and branding, most campaigns have done little to 
            develop a grassroots, sustainable and holistic programme with the 
            buy-in of all key stakeholders in the country. <br>
            However, a few such programmes in recent years have broadened their 
            approach and met with substantial success. Spain's national 
            promotion programme, identified by its striking logo (using Joan 
            Miro's &quot;Sun&quot;) is a good example of how a well-planned branding 
            programme can dramatically reposition a country over a limited 
            period of time.<br>
            National branding programmes can be developed to support a wide 
            range of national priorities, but primarily focus on promoting the 
            economic growth.<br>
            <br>
            <b>Brand Oman - the opportunity</b><br>
            Is branding relevant in the context of Oman's economic development? 
            Let's first look at the current perceptions of the Oman brand. Ask 
            people outside of Oman what they know about Oman, and you may get 
            very little in return; ask them to name an Omani product, and you 
            are unlikely to get anything other than oil and maybe a few comments 
            about Omani lobster or frankincense from some of those in the know. 
            Ask them about tourism opportunities in Oman, and almost nobody 
            outside of Oman will be aware of the cultural heritage, the scuba 
            diving, the wadis or the Khareef season in Salalah.<br>
            In short, Oman is a small story, poorly told. An Oman brand, if it 
            does exist, is extremely subtle. In terms of both tourism promotion 
            and attracting foreign investment, Oman faces the challenge of 
            competing against neighbours like Dubai and Qatar who have dedicated 
            more resources to their brand buliding exercise. In this context, 
            building an effective national brand will be critical for Oman to 
            &quot;get on the map&quot; of potential investors and tourists.<br>
            In terms of promoting Omani exports, a national brand offers a 
            number of potential benefits. Most importantly, it can be a critical 
            image-maker for exporters whose products have had little exposure to 
            international markets and for buyers who have never before bought 
            Omani products. If managed appropriately, it can also allow Omani 
            exporters to collectively develop a reputation for quality that can 
            be earned much more quickly than could be done individually.<br>
            <br>
            <b>Starting with the home market</b><br>
            It is in the domestic market that the development of Brand Oman can 
            have the biggest initial impact. Omani society is in the midst of 
            major changes - economically, it continues to diversify and begins 
            to look beyond its own borders; socially, it is leading the movement 
            of participatory government in the region; demographically, 
            continued population growth means that Oman is increasingly a nation 
            of youth. This is a story of growth, openness and modernity. At the 
            same time, the image of Oman to Omanis remains much at odds with 
            this picture - few Omanis see their brand image as modern, strident 
            and strong.<br>
            For Oman to be successful, both economically and socially, it is 
            critical for its citizens, consumers, businesses and workers to 
            develop a positive concept of what Oman stands for and where it is 
            going. We must first develop this internally before we can hope to 
            convince the rest of the world.<br>
            Development of a strong Omani brand in the domestic market can also 
            have an immediate impact on economic development and job creation. 
            The examples of South Africa and Australia show that raising 
            awareness of locally made products can be successful and can be a 
            major catalyst for economic development. Such programmes can then be 
            effectively leveraged into international markets to promote exports. 
            The &quot;Omani Made - Our First Choice&quot; programme is a potential 
            starting point for this. This programme, if harnessed effectively 
            and re-launched, can serve as a platform for the development of a 
            strong and dynamic Oman.<br>
