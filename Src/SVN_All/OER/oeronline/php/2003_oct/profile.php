                    <p><font face="Verdana" size="1">
                    <img border="0" src="im40.jpg" align="left" hspace="6" vspace="6" width="134" height="175">If 
                    it was possible to squeeze a lifetime of experience into a 
                    decade, this man has done it. All of 33, Khalid bin Mohammad 
                    Al-Zubair is sitting pretty atop the highest elected 
                    position in the private sector as the vice-chairman of Oman 
                    Chamber of Commerce &amp; Industry (OCCI). &quot;I've always lived 
                    two lives - one with my friends of the same age-group, and 
                    the other dealing with much more mature and older people 
                    from the business community,&quot; he admits. <br>
                    Now, however, he is glad that his generation has started 
                    making it big in Oman's work environment - something that he 
                    managed way back in the early 1990s. Try pointing that fact 
                    to him, though, and you are bound to get the &quot;I've been 
                    lucky to get this kind of exposure,&quot; response from the 
                    ever-modest director of The Zubair Corporation. <br>
                    Ten minutes into a conversation with Khalid Al-Zubair, and 
                    you realise that this man is much more complex than he may 
                    have seemed initially. He's intense, a firm believer in hard 
                    work, and what I have come around to call a 'stereotype 
                    buster'. &quot;There's always a better way to do things,&quot; he 
                    proclaims. &quot;And a better thing to do,&quot; he adds for effect.<br>
                    It is only natural, then, for a man who doesn't believe in 
                    the ordinary to think 'out of box'. &quot;Nothing is perfect in 
                    this world - neither can it ever be. And that's a good 
                    thing. I see that as an opportunity to make things better 
                    all the time,&quot; muses Al-Zubair. <br>
                    <br>
                    <b>Learning the ropes</b><br>
                    No, he isn't your typical heir-in-waiting who sits in a posh 
                    office and makes smart statements for public digestion. Far 
                    from it, Khalid Al-Zubair is a well-educated individual (he 
                    did his Bachelors in Business Administration with a major in 
                    Economics from the US) who entered his family business at a 
                    junior level and slogged it out there under different 
                    professional managers. &quot;That provided a solid background for 
                    me as I got to understand what it took to be in such an 
                    environment,&quot; he says. <br>
                    While his primary responsibility was, indeed, at a junior 
                    level, being part of the family also meant that he was 
                    exposed to the decision-making process at the board of 
                    directors and top management level. &quot;The fact that I 
                    participated in top management discussions from Day 1 into 
                    the job while working at a junior level proved doubly 
                    beneficial,&quot; claims Zubair. &quot;It resulted in me gaining 
                    accelerated experience.&quot; It's easy to see why. Being part of 
                    a decision-making entity while, at the same time, being at 
                    the receiving end of the implications of those decisions can 
                    broaden your perspective like no classroom coaching can.<br>
                    &quot;It provided me a solid foundation and also taught to be 
                    goal-oriented,&quot; he maintains. &quot;Not just the ultimate 
                    objective, which should never go out of focus no matter 
                    what,&quot; he is quick to clarify, &quot;but also targets that are 
                    specific to the tasks at hand.&quot; Only then can you &quot;do the 
                    right things in the right way.&quot;<br>
                    Al-Zubair began with the Finance &amp; Investment department of 
                    the group, and then had a stint with the Furnishing 
                    business, moving on to the IT and Telecom division. Only 
                    after he had fully understood the group's functioning and 
                    had gone through the proverbial grind did he return to 
                    manage the Furnishing business. Today, he's out of active 
                    operations and plays a strategic role in business 
                    development of the group, that looks at the available 
                    opportunities as well as ways of creating new ones. <br>
                    <br>
                    <b>Initial challenges</b><br>
                    Perhaps it was the knack of putting things in perspective 
                    and looking at them in 3D that made Khalid and his brothers 
                    realise early on after learning the ropes of the business 
                    that the group was at a stage where a lot of things needed 
                    to be re-looked at. &quot;The first major challenge that we faced 
                    was how to build on the sound foundation and assist in 
                    taking the group forward in the new way of managing 
                    businesses,&quot; remembers Al-Zubair.<br>
                    &quot;A lot of major changes were waiting to happen. We started 
                    looking at what businesses had a future and which ones had 
                    just been there traditionally, considering our core 
                    competencies and group strengths,&quot; he says. &quot;The challenge 
                    was to leverage the diversified base of the group, to make 
                    it our strength rather than the dilution of focus,&quot; he 
                    avers. <br>
                    Those analyses prompted some harsh decisions - some 
                    businesses were sold because it was felt that the group was 
                    not in a position to add too much value to them, some 
                    departments were closed down and a few were merged. &quot;We made 
                    sure that, at the end of it all, each of the businesses was 
                    autonomous, having its own management structure.&quot;<br>
                    <br>
                    <b>Management approach</b><br>
                    &quot;I don't like to centralise authority,&quot; affirms Al-Zubair. 
                    &quot;However, I spend a lot of time in analysing why things are 
                    done the way they have been done traditionally, and try and 
                    look for alternative ways in which they can be done.&quot; Al-Zubair 
                    follows what may be termed as 'management by questioning'. 
                    &quot;I ask a lot of questions - questions that provoke out of 
                    the box answers,&quot; he says. <br>
                    &quot;This helps put in perspective where the business currently 
                    is, where it can be taken and what new things can be done 
                    besides doing the old things in a better way,&quot; he claims. He 
                    believes that an organisation should not be bogged down by 
                    bureaucracy while at the same time have systems that can run 
                    independently.<br>
                    &quot;For instance, if I work with a manager,&quot; he explains, &quot;we 
                    will have heavy and prolonged interactions initially, 
                    necessary for us to understand each other enough to put in 
                    the right systems in place,&quot; he says. &quot;After the 
                    expectations have been communicated and competencies 
                    understood, it would be left to him to follow the system.&quot;<br>
                    &quot;However, communicating expectations is easier said than 
                    done. Business is like understanding a culture,&quot; reflects 
                    Al-Zubair. &quot;It's complex. You can't pass on your culture in 
                    a day or two,&quot; he says. &quot;Similarly, you cannot just ask a 
                    set of questions and take a few instructions and believe 
                    that you have understood what business is all about. It is 
                    extremely fluid, evolving all the time. Setting specific and 
                    clear goals is the beginning of a reliable foundation,&quot; he 
                    says.<br>
                    <br>
                    <b>The employee factor</b><br>
                    &quot;At the expense of sounding clichéd,&quot; says Al-Zubair, 
                    &quot;employees are our partners.&quot; According to him, &quot;Our role as 
                    shareholders should be to set the right environment in which 
                    to run a business, and not to actually run the business 
                    ourselves,&quot; he maintains. &quot;At the end of the day, who better 
                    to run your business than the professionals that you have 
                    handpicked and thus should have faith in their 
                    capabilities?&quot; he asks.<br>
                    &quot;However, it isn't enough just to do this kind of 
                    lip-service and believe that it will happen on its own,&quot; he 
                    cautions. &quot;Management has to put in a lot of effort to sell 
                    this idea to the employees, to make them believe in it - and 
                    that can only be communicated through the way you operate, 
                    the way you conduct your affairs. You have to find ways to 
                    come across as genuine in your beliefs because, at the end 
                    of the day, it is what they think that matters more than 
                    what you think that matters.&quot;<br>
                    &quot;That, obviously, entails constant realigning of business 
                    goals - be it strategic, financial or operational.&quot; Al-Zubair 
                    is a firm believer that it is vital that employees feel that 
                    they are partners in the progress of their organisation 
                    because only then will the long-term success of the company 
                    be ensured. <br>
                    <br>
                    <b>The OCCI feat</b><br>
                    Al-Zubair's most satisfying moment so far arrived recently 
                    when he was elected the vice-chairman of OCCI. &quot;It's a 
                    matter of great pride that I have been elected by the 
                    private sector as their representative in this honourable 
                    forum,&quot; proclaims Al-Zubair. He has been an active member of 
                    the Chamber for four years, and his networking and 
                    socialising skills may have had a role to play in his 
                    election. For the record, he's been participating in summits 
                    like the Five-Year Plan etc. besides being the chairman of 
                    associations like the Al-Ahlia Club.<br>
                    According to him, &quot;the larger the exposure one gets and the 
                    more people know you and know of you, the bigger the 
                    benefit. It's a reflection of what people think.&quot; Little 
                    wonder, then, that he has been actively participating in 
                    discussions and debates about diverse issues with various 
                    government and non-government bodies. &quot;While I've been lucky 
                    enough to do that, I am extremely committed to any task that 
                    I'm a part of,&quot; he states. <br>
                    Commenting on the tasks ahead of him as the vice-chairman of 
                    OCCI, Al-Zubair maintains that the private sector plays a 
                    vital role in the overall economic development of the 
                    country, and stresses on the need for the government to play 
                    less of an operational and more of a facilitatory role. &quot;In 
                    this regard,&quot; explains Al-Zubair, &quot;the Chamber can and must 
                    play a much bigger part.&quot; For that to become a reality, 
                    however, he is aware of the fact that the credibility of the 
                    Chamber will have to be taken to new heights. &quot;Private 
                    sector has to believe in OCCI. I like to manage 
                    expectations, but it does take a lot to change perceptions,&quot; 
                    he says pensively.<br>
                    &quot;OCCI is a consultative body. It takes feedback from the 
                    private sector, and then shares, analyses and recommends 
                    solutions. These recommendations can be much more emphatic 
                    if they are made with the collective will of its members. We 
                    hope to become a catalyst for the government,&quot; he avers.<br>
                    <br>
                    <b>Personal life</b><br>
                    Married with two school-going children, Al-Zubair plays as 
                    hard as he works. &quot;I have as active a social life as I have 
                    my work life,&quot; he confesses. &quot;Except Fridays, which I like 
                    to reserve for myself and my family, most of my evenings are 
                    spent attending functions and other social dos,&quot; he says. 
                    &quot;It includes going out and doing things with my kids or 
                    attending dinners with my wife or inviting friends or family 
                    members to our house.&quot;<br>
                    Al-Zubair's hobbies include, surprise surprise, playing 
                    football, for which he takes time out thrice a week to play 
                    in Boushar with a bunch of friends. And he's been playing 
                    the game since he was very young, right from the time he was 
                    in school. Apart from that, he enjoys reading business books 
                    as well as Arabic poetry. &quot;Business books excite me a lot, 
                    and I sometimes finish voluminous tomes in a single 
                    sitting,&quot; he says.</font></p>
