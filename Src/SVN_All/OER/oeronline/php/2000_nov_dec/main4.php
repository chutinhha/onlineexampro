                  <td width="100%"><font face="Verdana" size="4" color="#FF0000">Charting a Successful Course</font><p><font face="Verdana" size="2"><img border="0" src="../../images/2000_nov_dec/success.jpg" align="left" hspace="5" vspace="5">Creating a modern container terminal at Salalah in just 14 months was a remarkable feat of civil engineering. But developing it into the region's pivotal hub may prove to be less easy to achieve. However, a master plan incorporating a new free trade zone for Salalah promises to generate tens of thousands of imports and exports once established.<br>
                    <br>
                    Oman's government signed a memorandum of Understanding (MOU) with Salalah Port Services Company SAOG (SPS) in September naming SPS as the project manager for Salalah Port Free Zone (SPFZ) while US-based Hillwood Development was named as the specialist developer for SPFZ. SPS and Hillwood are developing a master plan for the entire free zone at Salalah. It will determine what additional infrastructure may be necessary to support the project as well as develop the business, financial and marketing plans for the venture and form a new operating company to run the development called Salalah Port Free Zone Company SAOC.&nbsp;<br>
                    <br>
                    The initial work on the first phase of SPFZ will develop approximately 180 hectares and construction will begin in Spring 2001. When complete, phase one of SPFZ is expected to increase the port's cargo activity, stimulate economic growth and development within the region as well as create job opportunities and provide local investment opportunities to Omanis.<br>
                    <br>
                    HE Minister of the National Economy Maqbool bin Ali Sultan hails the occasion as a turning point in Oman's drive to diversify its economy in non-oil sectors, saying: "The establishment of Salalah Port Free Zone is a natural and inevitable evolution of the events that led to the establishment of the container hub at Port Salalah two years ago."<br>
                    <br>
                    "Our current estimate of infrastructure cost in phase one is approximately $US89 million. We are not far enough along to estimate the level of client investment at this time," says the Jack Helton, chief executive officer of SPS. Total client investment in the two free trade zones of Sharjah - Saif and Hamriyah - is around Dhs3.5 billion from an initial investment of Dhs500 million.<br>
                    <br>
                    In addition to the free zone, a panel of experts led by the Japan International Cooperation Agency (JICA), has embarked on a landmark study aimed at helping Oman's government harness the full developmental potential of Salalah's modern container terminal. JICA, a government organisation that oversees Japanese developmental assistance to international countries, is also funding the one-year project under a bilateral cooperation arrangement between the Omani and Japanese governments.<br>
                    <br>
                    The master plan study of Salalah Port and its hinterland sets out three core objectives: (i) to update the existing master plan for Salalah Port (ii) to formulate a hinterland developmental plan focusing on the port, and (iii) to recommend mechanisms for Japanese technology transfer to Oman, notably in the area of port management and policy making.<br>
                    <br>
                    In essence, the study is intended to ensure that the master plan for the port dovetails with the planned development of industry and services around the port, according to Mitsuhiko Okada, deputy chief of the JICA mission.&nbsp;<br>
                    <br>
                    "Clearly, Port Salalah will play a strategic role in the development of the hinterland, which must go hand in hand with its own development. Our task is to make recommendations and provide the necessary inputs for a final blueprint on the future development of the port and its hinterland," he says.<br>
                    <br>
                    According to JICA, the master plan aims to achieve the formidable goal of attracting 15 million TEU of transshipment cargo a year. Support services such as banking, insurance, cargo clearance and container repairs are also expected to flourish. The study will outline a number of scenarios on the potential for industrial and commercial development in the port's hinterland. A broad range of activities are planned such as fish and food processing, garments for the European market, manufacture of construction materials for South East Asian countries, re-export targeting the Indian Ocean Rim and tourism.&nbsp;<br>
                    <br>
                    Industrial activity in the hinterland has the potential to boost GDP growth by at least RO100 million, while also generating more than 20,000 jobs by 2020. Likewise, service sectors such as transportation, communications and tourism, in addition to general commerce, are projected to make an equally significant impact.&nbsp;</font></p>
