                  <td width="100%"><font face="Verdana" size="4" color="#FF0000">Marine Tourism</font><p><font face="Verdana" size="2">
                    <img border="0" src="../../images/2000_nov_dec/marin.jpg" align="left" hspace="5" vspace="5" width="200" height="179"></font><font face="Verdana" size="2">The natural richness and diversity of Oman's coastal zone has yet to be fully developed but holds great potential value for Sultanate's continued economic development, say industry experts. While some maintain a cautious approach to growing aqua tourism, others are more optimistic saying a balance can be reached to increase domestic revenues, preserve local traditions and protect ecosystems. In a bid to boost sustainable tourism, Oman is currently planning to introduce new laws to better plan, regulate and develop activities within its marine environment.&nbsp;<br>
                    <br>
                    "The Ministry of Regional Municipalities and Environment (MRME) and the Ministry of Agriculture and Fisheries (MAF) are both looking closely at developing new conservation laws that will be more comprehensive," says Salim Al-Saady, director of Biodiversity in Directorate General of Nature Conservation, MRME. "Probably one of the most important marine protected areas (MPAs) would be the Barr Al Hikman and Masirah Island area. This is important for wetland birds, turtles, coral reefs and others. This location is currently being proposed as an MPA. The policy of promoting sustainable utilization of these types of areas, in accordance with Oman's policy of controlled domestic tourism, is one of our major objectives."&nbsp;<br>
                    <br>
                    "Such new measures will raise local awareness among locals as well as international tourists," says Robert Gardner of Oman's Diving Adventure Centre. "The initiative is likely to impress European divers who like to see strong laws enforced and who are overall very impressed with the quality of facilities here compared to Egypt for example."<br>
                    <br>
                    Technically, Oman currently has two MPAs issued by Royal Decree: the prime diving location of the Daymaniyat Islands and Ra's Al Hadd, one of the most important turtle nesting sites in the Indian Ocean. Visitor access is restricted through the issue of entry permits which are checked by Royal Oman Police (ROP) on the Daymaniyats and by MRME rangers on Ra's Al Hadd. Al-Saady says that because the MRME incorporates coastal lagoons into its definition of marine environment, the Sultanate technically has a further nine MPAs in the Dhofar region. An MPA recently established in Muscat to protect mangroves takes the tally to 12 but many more are proposed.<br>
                    <br>
                    In terms of tourist activity at these sites, the fledgling whale and dolphin watching operations in Muscat generates approximately RO45,000 per year while the nature reserve at Ra's Al Hadd received approximately 10,000 visitors in 1999, each paying a RO1 entrance fee. Moreover, about 3,000 divers visited the Daymaniyat Islands National Nature Reserve (DINNR) last year generating approximately RO50,000 for the diving industry, according to a recent draft report by Oman's Marine Biodiversity Sub-Committee (OMBS).&nbsp;<br>
                    <br>
                    However, a series of Royal Decrees (including the Law on Conservation of the Environment and Prevention of Pollution, the Law on National Parks and Natural Protected Areas, the Law on Marine Fisheries and the Living Aquatic Resources Protection Act) together with other legal provisions have created an initial framework for protection and conservation of MPAs. Al-Saady says the overall environment management structure now includes a National Conservation Plan covering marine areas, a Marine Coastal Management Plan and a National Biodiversity Strategy which identifies areas of immediate threat and flags them for priority action.&nbsp;<br>
                    <br>
                    The development of the tourism industry in the Muscat area includes proposals to increase numbers of diving tourists. While the number of divers currently is low, pressure on coral communities may result from careless diving practices such as direct contact or breakage of coral colonies by divers and anchoring of boats on coral. Areas under threat are Fahal Island, Daymaniyat Islands, Hoons' Bay, Cat Island and Bandar Khayran, Baldwin says.<br>
                    <br>
                    "Diving holds great potential and there is plenty of opportunity to offer dedicated diving holidays or diving as an exclusive holiday package deal. The biggest single impact on coral is fisheries while diving impact is significantly small in comparison but could become a hazard in the future. Because Oman has a limited fringing coastal reef, areas of interest are dived more intensively in terms of frequency," says Robert Baldwin, environmental specialist with WS Atkins International and Co. He says that extending the existing mooring buoy network to include diving sites and publishing guidelines reminding divers of the code of conduct would better serve to protect the marine environment.&nbsp;<br>
                    <br>
                    "Developing small-scale specialist group tourism, rather than mainstream tourism, will bring in visitors who are more educated and responsible as well as culturally and environmentally sensitive," he adds.&nbsp;<br>
                    <br>
                    "There is a lot more that could be done in terms promoting diving holidays in Oman. For example, the sinking of a large ship or war plane down the coast may bring the opportunity for another dive region away from Muscat and pull in more divers generally," says Gardner.<br>
                    <br>
                    Elsewhere in the world, primarily in Egypt and the Caribbean, indications are that the carrying capacity of reefs for divers is in the order of thousands per dive per year per kilometer of reef, which is far in excess of the density of divers in Oman. Indeed, there is a positive impact is some areas, where divers are responsible for the removal of abandoned fishing nets entangled in coral and lobster traps on the seabed as well as managing beach clean-ups in general.&nbsp;<br>
                    <br>
                    In the Seychelles, a 10-year investment programme called the Environmental Management Plan of the Seychelles (EMPS) has designated five nationally protected marine areas and identified many problems despite the legislative framework in place. Increased coastal tourism has damaged corals due to anchor drops and illegal collecting. Paradoxically, as tourism increased, the infrastructure to welcome visitors had deteriorated with one marine education centre in St. Anne Marine National Park left virtually abandoned. The EMPS aims to update and publish conservation information, demarcate MPAs, install 75 buoys, rehabilitate the education center, establish underwater trails and facilitate more surveillance and enforcement of the marine parks.<br>
                    <br>
                    "Dolphin watching and whale watching could also boom," says Baldwin. "There are 80 to 90 countries around the world where tourists can book specialist holidays for this activity." Ironically, however, the noise of speed boats, fishing activities and expanding shipping lines may disturb the migration of these species and damage any potential commercial returns unless managed and monitored carefully.<br>
                    <br>
                    According to a report conducted for the MRME by the Mouchel Group Environmental Consultancy, there are also conflicts between heavy boat traffic and other user groups like swimmers, jet-skiers and water-skiers. Water-skiing in particular appears to be inappropriately placed close to some public beaches. In Ra's Al Hamra, it recommends that this marine activity should be controlled and a sense of exclusive ownership to the operators prevented from happening.<br>
                    <br>
                    It is estimated that the region's boating industry is growing by over 5% per annum. There are about 32 existing marinas and boating clubs in the GCC and a further eight in the planning and construction stage. "The fundamentals for significant growth in the region exist. There is ideal weather, good sea conditions and safe havens from offshore breeze available. Added to this, there is the boost from the rise in oil prices," says George Williams, general manager of Marina Bander Al Rowdha SAOG. He says about 10.4 million people - excluding expatriates - possess enough purchasing power to participate in the recreational boating in the Gulf.<br>
                    <br>
                    According to marine biologists, turtle watching could also be a more lucrative money spinner. However, turtle-nesting habitats along with other coastal beaches are also at risk. The coast of Oman is exposed to increasing private development from hotels to housing. Erosion of soft sediment and destruction of local ecology is increasing significantly as is littering which constrains the potential for ecotourism in such areas.<br>
                    <br>
                    "Along the Batinah coast in the north, the beaches are eroding. Other management problems relating to beaches include disturbance of turtle breeding areas, increasing development close to the shore, reduction in sediment supply and chronic oil pollution. Off road vehicles have uncontrolled access in most areas and are a threat to mobile epifauna," are the findings of another OMBS study.</font></p>
