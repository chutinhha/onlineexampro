                  <td width="100%"><font face="Verdana" size="4" color="#FF0000">Keeping Watch</font><p><font face="MS Sans Serif" size="2"><img border="0" src="../../images/2000_nov_dec/keeep_watch.jpg" align="left" hspace="5" vspace="5" width="129" height="240">The Gulf's shipping industry has remained relatively buoyant in the first seven months of 2000 and the outlook for future growth is bright. Total freight handling at ports within the region and container throughputs in Oman and the UAE have mostly risen. Moreover, activities such as hinterland developments and free trade zone (FTZ) projects as well as the construction of new ports within the Middle East are forecast to dramatically increase during the next two to three years.&nbsp;<br>
                    Meanwhile, existing container terminals including Salalah are ploughing back large chunks of their profits into port expansions to prepare for increasing global demand and to service the new generation of mega-container ships expected to begin commercial operations around June 2001.<br>
                    "We believe the market is strong enough for all of the ports to survive. However, some of the ports will have to adapt as the container ships get bigger and bigger," says Jack Helton, chief executive officer of Salalah Port Services (SPS) who will oversee the development of Salalah's planned FTZ. "We are continually monitoring what our competition is doing both in the ports and free zones. We are using them as benchmarks and I am sure they are trying to make sure they do not loose market share as a result of the developments in Salalah."&nbsp;<br>
                    <br>
                    Maritime officials also emphasize the continued importance of general cargo ports such as Oman's Sultan Qaboos Port in Muscat and the UAE's Fujairah Port for easier distribution of freight to important land-based markets, providing complementary container cargo for trans-shipment at bigger main line ports, and carrying containers to and from outports.&nbsp;<br>
                    <br>
                    "It is often said that the future belongs to big lines, large vessels and huge ports," says a senior official at Fujairah Port. "What is often ignored in this assessment is that there will continue to be a demand, and probably a growing demand, for efficient feeder services and secure yet reliable relay technique which satisfies the needs of both the shipping line and, most importantly, its customer."&nbsp;<br>
                    <br>
                    Despite the competitive environment, experts say that the investment will meet future demand as world container volumes are set to double during the next 10 years. During the next 15 years, UK-based Drewry Shipping Consultants project huge growth of 142% in trans-shipments and 78% in gateway traffic in the Middle East.&nbsp;<br>
                    <br>
                    Levels of service<br>
                    "The number of well-equipped ports in the region will ensure that competition remains fierce," says Brian Coughlan, director and general manager of Gulftainer Company Limited. "This is advantageous to the shipping lines who can shop around for the best deal, both financially and for level of service."<br>
                    <br>
                    Sultan Bin Sulayem, Chairman of Jebel Ali Free Zone (JAFZ) and Managing Director of Dubai Ports Authority (DPA) agrees that the survival of container terminals in the Middle East will become increasingly dependent on customer service. He mentions that DPA's ability to keep customers satisfied has been enhanced by the introduction of two state-of-the-art solutions: A digital market place called Tejari.com launched in September and DPA's Internet portal.&nbsp;<br>
                    <br>
                    "The ever-changing face of technology in the port and shipping community is no longer an option," says Bin Sulayem "The recent IT developments at DPA represents our ability and willingness to embrace spectacular growth."&nbsp;<br>
                    <br>
                    A local focus<br>
                    The DPA chief says he has focused on several strategies to create high productivity and counter the regional competition: The differentiation of its operations from other terminals, a focus on the importance of land cargo and flexibility towards its dependence on trans-shipment cargo. Since 1990, DPA's trans-shipment sector approached 60% of operations but this figure has fallen to around 42% recently with more attention being given towards the solid foundations of the domestic market.&nbsp;<br>
                    <br>
                    Oman's port of Salalah, however, main purpose is currently on growing international trans-shipment, unpredictable though it might be due to its reliance on brisk economic activity in other countries for stable trade and business. According to figures released by the Ministry of the National Economy, cargo unloaded at the Sultan Qaboos Port rose by 8.0% in the first seven months of 2000 while freight at Salalah Port slightly decreased by 3.5% during the same period. However, forecasts for Salalah in terms of container throughput look much more favourable: 1.2 million TEUs for 2000, 1.6 million for 2001 and 2.0 million for 2002.&nbsp;<br>
                    <br>
                    "Our next targets are reaching 2.2 million TEUs in 2001, expanding berths five and six by 2003, and creating 3,000 jobs in the next five years," says Helton who says that these objectives will be done through the development and subsequent promotion of Salalah Port Free Zone (SPFZ).<br>
                    <br>
                    Aside from container terminal expansions, new ports to support industrial zones and regional diversification away from oil dependence are planned. Oman's Sohar Port is set for completion by the end of 2001 (See OER May/June) but is expected to be ready to receive ships as early as June 2001.&nbsp;</font></p>
