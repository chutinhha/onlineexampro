    <p align="center" style="margin: 10">
    <b><font face="Verdana" style="font-size: 18pt">Tale of Three Cities</font></b><p align="justify" style="margin: 10">
    <font size="1" face="Verdana">Weekend parties in Muscat are incomplete 
	without a bowl of lament, some ruing and a generous helping of fulminate. 
	Visits to Dubai, Manama or Doha in the recent past inevitably adds fuel to 
	the debate: which is the better city? Needless to say, who the loser is. <br>
	<br>
	Cities, or for that matter, capitals are something similar to human beings. 
	No two metropolises are the same because each has own their history, 
	geography and last but not the least, a vast amount of culture. Dubai, among 
	the three cities under the microscope, is the tallest in terms of its 
	cosmopolitan look and feel. Skyscrapers. Construction boom in full swing. 
	Roads with the latest cars. Beaches. Bars. Discotheques. Active night life. 
	Vast malls. A shopper’s paradise, so to say. The atmosphere reverberates 
	with money, money and money. Or, put it differently, Dubai spells business 
	to the core. <br>
	<br>
	Or take for that matter, Manama, the pear-shaped isthumus north of Oman. 
	Glass-fronted office edifices. Gigantic malls that entice the eastern Saudi 
	Arabia residents through the ever-crowded King’s Causeway. Night life that 
	emulates Dubai. Ever since Lebanon got enmeshed in internecine warfare way 
	back in the 1970s, Manama quietly moved into the vacuum. Banks and insurance 
	companies jostle with one another to generate business not only within 
	Bahrain but catering to the entire world. <br>
	<br>
	Another significant and striking factor not to be missed is the freedom of 
	expression. Bahrain boasts of the most vociferous Fourth Estate, with 
	newspapers that for a moment make one feel that one isn’t in the Kingdom of 
	Bahrain but in Europe or America, the citadel of democracy. The media in 
	United Arab Emirates is trying to catch up. And who does not know about Al 
	Jazeera TV channels from Qatar – the hugely popular Arabic and the newly 
	launched International (in English) that got rave reviews? Of course, this 
	greater freedom is conditional to internal restrictions – but getting more 
	responsive than one could hope to expect in Oman. Free press means more 
	brickbats – and bouquets. Not the daily dose of sugar-coated news. Tolerance 
	is the name of the game, perhaps.<br>
	<br>
	Mendicants with their extended bowls seeking alms on the main thoroughfare 
	of Manama is nothing unusual. Sighting Bahrainis in dish-dasha is a rare 
	sight, unless one moves into the interior of the island. Similarly in Dubai. 
	Traffic discipline as we are used to on Oman thoroughfares is an alien 
	concept in Dubai and Manama, what with honking of horns at the signals, 
	jumping lanes, parking in the middle of the road obstructing free flow etc. 
	It is not uncommon to witness extremists demonstrating against anything 
	liberal including the presence and performance of Lebanese singers in 
	Bahrain. Oman, on the other hand, is spared much of this.<br>
	<br>
	One more thing to bear in mind is that the comparison is not fair. Why? The 
	UAE, Bahrain and Qatar are more of city states with very small indigenous 
	population and large multi-ethnic population. That is why Dubai and Manama 
	have become more of cultural melting pots. Expatriate domination has heavily 
	impacted the local culture in Bahrain and the UAE. It is an open secret that 
	these cities have lost much of their ‘Arab’ness in their pursuit of wealth 
	creation. A heavy price to pay.<br>
	<br>
	Knowledgeable Emiratis and Bahrainis shower huge encomium on His Majesty 
	Sultan Qaboos bin Said for his ability to retain the tradition and culture 
	despite roping in the expertise of expatriates in building the modern 
	Sultanate. Oman may not be on the same scale as Bahrain and UAE. It is 
	perhaps more than apt to call Oman the cultural capital of the Gulf region. 
	I hope the fickle among you are listening.</font></td>
