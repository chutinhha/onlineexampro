				<tr>
					<td bgcolor="#E1E1E1" height="26">
					<p style="margin: 5px 10px" align="justify">
					<b><font face="Verdana" size="1">Dr Jerrold 
					D. Green, senior advisor (Middle East/South Asia region) to 
					the RAND Corporation, in an exclusive interview with Abhijit 
					Sinha, says that the heart of American relationship with 
					most of the Gulf States cannot be divorced from the nature 
					of the economic interactions</font></b></td>
				</tr>
			</table>
			</div>
			<p align="justify" style="margin-left: 10px; margin-right: 10px">
            <font face="Verdana" size="1">An academic by qualification, Dr 
			Jerrold D. Green was professor of political science and taught at 
			the University of Michigan and University of Arizona. He also served 
			as the Director of Middle East Center in Arizona University, and 
			also served as Fullbright professor in Cairo University, along with 
			other academic stints in Tehran and Tel Aviv. Three decades of 
			studying the Levant and the Middle East as an academic has focused 
			his interest in inter-Arab relations, Iranian politics, and the 
			US-Middle East policies. Excerpts of the interview:<br>
			<b><br>
			How do you see the global fallout on the energy security issue, with 
			concern being voiced on the eventual depletion of the Hydrocarbon 
			age? What should the Gulf nations do in the next five years?</b><br>
			I think everyone agrees, including major producers of fossil fuel 
			energy, that aggressive exploration of alternatives to fossil fuel 
			energies is to everyone’s benefit including the producers. I just 
			read a very interesting paper by a Saudi analyst where he says Saudi 
			Arabia needs to explore solar, wind-power and other forms of 
			energies for its own domestic consumption. That’s very wise. Now, 
			there’s this GCC initiative to explore nuclear power – it makes very 
			good sense. Oman in particular has finite supplies of fossil fuel 
			and is aware of the fact that it needs to diversify its economy 
			domestically.<br>
			<b><br>
			Diversification means trade and commerce. Signing the FTA with the 
			U.S., how does it benefit Oman socio-economically?</b><br>
			Anything that facilitates trade, and relations between the U.S. and 
			Oman, is really good. I believe in the open market and enhanced 
			commerce. Free trade agreement facilitates economic and financial 
			relations between the countries. Cooperation on other levels too is 
			enhanced – people to people, healthcare, education etc. For example, 
			in the ‘70s it was difficult to get a visa for Oman. Now it’s visa 
			on entry. So, a much more user-friendly place to visit – which is 
			very important for free trade. The FTA facilitates cooperation far 
			beyond the narrow confines of commercial relations.<br>
			<b><br>
			You have been coming to Oman for nearly three decades. What are the 
			socio-economic changes you have seen?</b><br>
			My perception is that Oman has successfully avoided a ‘Boom-Bust’ 
			mentality. It’s apparent to me, even as a visitor, that the National 
			Economic Planning here is done in a very logical, strategic and 
			well-designed fashion. Here, planning exists in a rational and 
			important manner. The country is developing in a very positive way.<br>
			<b><br>
			There’s been an exponential opening up in the ‘B2B’ sector in some 
			of the Gulf countries. What should be Oman’s option vis-à-vis 
			protection of its economy, environment and culture?</b><br>
			There are profound differences between every country in the Gulf. 
			For example, Kuwait is very different from Bahrain, which is again 
			very different from the UAE. In fact, Dubai is very different from 
			Abu Dhabi. Each country needs to select its route that is 
			appropriate for international development and its population. My 
			impression, as an occasional visitor, is that Oman is doing very 
			well.<br>
			<b><br>
			How will the Asian Games affect Qatar and the region?</b><br>
			Qatar is a country with a small population but significant revenues 
			from its energies. It recently hosted the Asian Games. That’s very 
			good for everybody as it was Qatar. That means in the Gulf. And that 
			consequently means it’s in one of the GCC countries. So, it is very 
			good for the entire region. Attention is focused on the region in a 
			good positive way.<br>
			<b><br>
			How do you see the effect of globalisation?</b><br>
			A very important factor in this era of globalisation is that the 
			neighbouring countries benefit as well. Like Mumbai is just 
			two-three hours by flight form the Gulf, and the Indian development 
			is complementary to the development of the Gulf – in terms of human 
			capital, commerce and a variety of related ways. Activities that 
			support globalization is for everyone’s collective benefit.<br>
			<b><br>
			So, how can this positive domino effect be translated in the setting 
			up of the Common Market in the Gulf? </b><br>
			It’s a very important question – and must be answered by the people 
			of this region. But if you look at the history of the EU, and the 
			gradual development and acceptance of the Euro, it was a long 
			process by states that are much older – older in the sense of being 
			independent.<br>
			<br>
			Things like currency; airlines etc are symbols of nationalism. For 
			example, it would be great to have single airlines for the entire 
			GCC, as there is a proliferation of airlines. Also, it is cumbersome 
			to have different currencies when traveling in the same region. But 
			then, these are separate distinct countries with their own history, 
			tradition and culture. So, if you lose your national currency, you 
			lose something of that history and culture.<br>
			<b><br>
			But every currency here is pegged to the US dollar?</b><br>
			Now, Iran has pegged its currency to the Euro. In Iraq, it’s linked 
			to the US dollar. If you look at the history of the Euro-dollar 
			exchange rates, you will see that there have been profound changes 
			there too. So, it is for the government and its people to decide 
			what is in their own best interest – and presumably they will opt to 
			weigh the value of having a common currency against the value of the 
			country having its own currency. These are small countries. Saudi 
			Arabia and Oman are large. But Kuwait, Qatar and the UAE are 
			territorially very small.<br>
			<b><br>
			So what happens to free trade?</b><br>
			It capitalises on the attractiveness of lowering the tariff 
			barriers, for safety and interacting with one another. But you 
			don’t’ want to lower the barriers in a way that may somehow be held 
			as sacrificing one’s individualism. So, how do you find your way? 
			It’s a complicated issue.<br>
			<b><br>
			What’s your perspective on the Cost-Push effect in the energy sector 
			for Year 2007 – especially for Oman and the GCC?</b><br>
			Oman is very sensitive to the fact that it has a finite fossil fuel 
			supply. It is my impression that there’s a significant effort to 
			diversify the economy to minimise the dependence on fossil fuel – 
			sale as well as usage.<br>
			<b><br>
			What will be the effect globally of this oil price fluctuation?</b><br>
			For one, I don’t see the oil price coming down. It’s been a 
			three-year run for the oil exporting countries, and the GCC has 
			gained in terms of the infrastructure building process. I see the 
			rates remaining high and I do not hold it against these countries – 
			for pricing their commodity at levels that can be paid. If things 
			are worth something, people are going to pay for them. Yes, rates 
			are high. But people are willing to pay for them. It’s a scarce 
			commodity – just like diamonds cost more than bread.<br>
			<br>
			That’s capitalism and I don’t have a problem with that. I think we 
			need to understand that the price of energy is the fulcrum to 
			economic interaction in a lot of places. Of course, the price of 
			bread is going to be affected by the price of energy – because you 
			need energy to bake it and to delver.<br>
			<br>
			Another reason for fossil fuel energy becoming expensive is because 
			there’s no viable alternative. These alternatives do exist, but they 
			have not been sufficiently developed. I don’t hold it against these 
			companies because they are doing well. I really think capitalism is 
			the way of the world, and we have to accommodate this fact.<br>
			<b><br>
			As a political scientist, how do you see the WTO affecting the three 
			factors now emerging in this region – the FTA, the Gulf Common 
			Market aspirations, and the positive domino effect of neighbours 
			which can also be termed as the ripple effect of the region?</b><br>
			There’s an increasing awareness of the importance of economic issues 
			and factors being part and parcel of political relations between 
			countries that seem to be less defined historically. There’s always 
			been trade and commerce between countries, and economy has always 
			mattered. Now it’s seen to be playing an even greater role. I regard 
			this as a positive development.<br>
			<br>
			For example, converse to the U.S. relations with India and Pakistan, 
			our stocks in the Middle East are not that high. There’s a 
			significant degree of antipathy towards the U.S. in this part of the 
			world for a variety of reasons – not all of them economic. I 
			consider the entire situation as more than unfortunate, it’s tragic.<br>
			<br>
			So, one can’t be too attentive to economic relations. Creation of 
			wealth is at the heart of issue, but relations between states cannot 
			be ignored.<br>
			<b><br>
			But will the WTO drive be at the cost of the US-GCC or US-Oman 
			relationship?</b><br>
			I don’t think so, though I am a political scientist. My impression 
			is that in many ways the heart of American relationship with a lot 
			the Gulf States cannot be divorced from the nature of the economic 
			interactions. Yes, there are strategic, political and other sorts of 
			things. But economic issues are certainly very very essential to 
			this relationship. That’s why I think the FTAs and the Common Market 
			etc will help achieve this region come out of the passivity by their 
			very bilateralism.<br>
			<br>
			Like the U.S.-Middle East policy, there’s also a U.S.-Oman 
			relationship, and that’s very important. Every country has its own 
			concern. I have never liked talking regionally because it tends to 
			devalue the individual qualities of individual nations. Each country 
			of this region has a vote in the United Nations, and it is important 
			to promote these bilateral relations. To me, it’s absolutely 
			imperative.</font></p>
