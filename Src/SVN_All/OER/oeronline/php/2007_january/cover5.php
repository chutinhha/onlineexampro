            </font><b>
					<font face="Verdana" size="5">‘Common Market Free Trade 
					Agreements &amp; Bilateralism will help this region’</font></b></p>
			<div align="center">
			<table border="0" cellpadding="0" style="border-collapse: collapse" width="95%" id="table18" height="281">
				<tr>
					<td bgcolor="#EFEFEF">
					<p align="center"><font face="Verdana" size="1">
					<img border="0" src="cover6.jpg" width="475" height="270"></font></td>
