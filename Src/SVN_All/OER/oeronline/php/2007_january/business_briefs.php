<font face="Verdana" size="2"><b>OEA’s Feb meet on Corporate Governance</b></font><font face="Verdana" size="1"><br>
Omani Economic Association (OEA), a professional organisation to promote 
economic research, is organising a conference on ‘Corporate Governance, 
Competitiveness and the Future’ on February 3 and 4, 2007, at Crown Plaza, 
Muscat. Speaking to Oman Economic Review, the Strategic Media Partner of the 
conference, the chairman of the association, Sheikh Mohamed bin Abdullah bin 
Hamed Alharthy, said that the conference would be of critical importance towards 
understanding, as well as embracing, the spirit of corporate governance in both 
public as well as private sectors.<br>
<br>
“We are at the midpoint of the Oman Vision 2020, and it’s high time we carried 
out a technical evaluation. The conference would discuss where Oman is heading 
to, what are the merits and possible lacunae of the Vision 2020, how we can sort 
out such shortcomings,” said Sheikh Mohamed.<br>
