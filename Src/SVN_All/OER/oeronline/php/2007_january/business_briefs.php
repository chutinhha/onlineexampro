<font face="Verdana" style="font-size: 10pt"><b>Muscat Festival to go BIG</b></font><font face="Verdana" size="1"><br>
<img border="0" src="bsbr1.jpg" width="150" height="95" align="left" hspace="5" vspace="5">Preparations are well underway for Oman’s biggest festival, which will take 
place in Muscat between January 1 and February 2, 2007. The Muscat Festival is 
an annual event that focuses on culture and tourism, and officials are promising 
that this year it will be bigger and better. Last year, the festival saw record 
attendance at the festival, with 2.2 million turning out, but organisers are 
confident that this figure can be surpassed.<br>
<br>
This year, Muscat Festival’s focus is on heritage and culture. “Muscat Festival 
has always been a window to showcase the cultural richness of the country, and 
this year too the emphasis will be on the various aspects of our culture, past 
and present,” said Abdullah bin Abbas bin Ahmed, president of Muscat 
Municipality. “There is a special significance to the festival this time, 
because it coincides with Muscat’s celebrations as the Arab Cultural Capital for 
2006. So the programmes and events will particularly focus on the city’s rich 
heritage and culture,” he added.<br>
<br>
The major attractions at the event will include 
an Oman cultural village, Arabian markets, 
international theatre, and a forum of 
peoples’ cultures. “This year’s 
festival will have lot of new fea-tures that makes it even more
interesting than its predecessors. The Heritage Village in Qurum 
will have a completely new and refurbished look,” added Abdullah.<br>
