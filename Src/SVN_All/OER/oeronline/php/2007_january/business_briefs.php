<font face="Verdana" size="2"><b>The Wave’s Phase II starts in January</b></font><font face="Verdana" size="1"><br>
<img border="0" src="bsbr5.jpg" width="250" height="125" align="left" hspace="5" vspace="5">After making bookings worth US $100 million for the first phase, The Wave 
launched the second phase of its sales on December 16, 2006. A total of 151 
residential units, comprising townhouses besides garden and water front villas 
in the price range of RO 115,000 to RO 450,000 with a combined value of US $85 
million, are available for booking on first-cum-first serve basis. According to 
a company spokesperson, more than 40 per cent bookings were already done before 
the sale was opened to the general public. Nick Smith, CEO of The Wave, 
informed, “Our pre-launch buyers include Omani and GCC nationals, expatriates 
working in the region and Europeans. The foundation work will start in January 
and the construction is expected to be complete by the first quarter of 2008.”
<br>
<br>
Talking about the first phase, Smith said, “The first residential block will be 
ready for possession by March this year.” The Wave, Muscat, is a world-class US 
$1 billion beachfront tourism and residential project stretching along 6 km of 
natural beach. It will include a string of man-made lakes and islands. The 
development will encompass a signature 18-hole green links golf course designed 
by renowned golfer and designer Greg Norman, a 300-berth marina, four luxury 
hotels, retail and restaurant outlets along with approximately 4,000 residential 
units. <br>
