<font face="Verdana" size="2"><b>Sohar Port gets Maritime Award</b></font><font face="Verdana" size="1"><br>
<img border="0" src="bsbr4.jpg" width="159" height="150" align="left" hspace="5" vspace="5">The Port of Sohar, developed with an investment of US$ 12 billion, has been 
felicitated with the prestigious Sea Trade Award for ‘Outstanding Success in 
Port and Terminal Operations’. The Sea Trade Awards are given to the best 
performers in the maritime industry from the Middle East Region and South Asia. 
Jamal Aziz, Deputy CEO, Sohar Industrial Port Company (SIPC), said: “Our success 
was due to the invaluable support of the Port of Rotterdam in the management by 
the port working with the Government of Oman to establish a modern 
infrastructure.” The Port of Sohar, an industrial port, situated 220 km 
northwest of Muscat, has added a new episode in the development of futuristic 
port facilities in Oman. Set up in 2002, the port along with its container 
project is one of the largest of its kind in the world. The Port is fast 
receiving international recognition because of the large number of industries 
and financial investment it has attracted. A key factor in its speedy growth has 
been its location outside the Straits of Hormuz. <br>
