DUBAI’S NICO in Kazakhstan Ship Repair Yard JV</b></font><font face="Verdana" size="1"><br>
<img border="0" src="bsbr.jpg" width="200" height="145" align="left" hspace="5" vspace="5">Three 
maritime majors have entered into a Memorandum of Understanding (MoU) to 
establish a joint venture to develop a vessel maintenance and repair facility on 
the Caspian Sea Coast of Kazakhstan. Dubai-based Nico International, a 
subsidiary of the Topaz Energy and Marine Group owned by Renaissance Services 
SAOG, a publicly listed Omani Company; Almaty-based Caspian Services Inc. (CSI); 
and, the national marine company of Kazakhstan, Kazmortransflot JSC (KMTF) 
signed the MoU at Dubai World Trade Centre. The facility is to be constructed on 
the grounds of the marine base currently under development by CSI at the port of 
Bautino in Kazakhstan. It will be designed to provide both dry docking and 
floating repair capabilities to the full range of vessels operating in the 
Caspian Sea, up to a maximum 60 metres in length and 600 tonnes ‘lightship’. The 
facility will also have the capability to dry berth eight vessels at one time. 
Nico International will develop and operate the facility on behalf of the JV.
<br>
<br>
Nico is a Dubai-based international ship repair and boat building company with 
30 years experience in the industry. With 1,200 employees and operations in four 
countries, Nico operates eight companies dedicated to the full spectrum of ship 
repair. Nico recently inaugurated a new marine workshop in Bautino which is 
staffed by Kazakh marine engineers and supervised by skilled Nico personnel. 
Nico General Manager Fazel A. Fazelbhoy stated: “We are excited at the prospect 
of setting up the first international level ship repair facility in Kazakhstan. 
We have already trained 12 Kazakh graduates from the Marine Academy in Aktau, 
Kazakhstan, in our workshops in Dubai and are now looking to further expand our 
horizons to the Caspian Sea. Our sister company BUE Kazakh operates over 40 
vessels and this is a logical extension of our service and investment commitment 
to a rapidly developing oil economy.”<br>
