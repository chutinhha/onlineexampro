            </b><br>
            </font><b>
					<font face="Verdana" size="5">The FTA Effect</font></b></p>
			<div align="center">
			<table border="0" cellpadding="0" style="border-collapse: collapse" width="95%" id="table17" height="281">
				<tr>
					<td bgcolor="#EFEFEF">
					<p align="center"><font face="Verdana" size="1">
					<img border="0" src="cover5.jpg" width="475" height="319"></font></td>
				</tr>
				<tr>
					<td bgcolor="#E1E1E1" height="26">
					<p style="margin: 5px 10px" align="justify">
					<font face="Verdana" size="1"><b>Though 
					there may be some short-term challenges associated with the 
					Oman-US FTA, but the benefits clearly outweigh liabilities</b><br>
					<br>
					By David Hamod, President and CEO, National US-Arab Chamber 
					of Commerce</font></td>
				</tr>
			</table>
			</div>
			<p align="justify" style="margin-left: 10px; margin-right: 10px">
            <font face="Verdana" size="1">In the United States we say, “You can 
			lead a horse to water, but you can’t make it drink.” What do we mean 
			by that? Even if we know that the horse needs water, and even if the 
			water is provided to the horse, it will not drink unless convinced 
			that drinking water is a good idea. Why have I cited this particular 
			saying? Because I see some parallels between it and the current 
			situation facing the US-Oman Free Trade Agreement.<br>
			<br>
			The Governments of Oman and the United States have negotiated a high 
			quality FTA, and now they are turning to the private sector to carry 
			the initiative forward. But not everyone in the US and Omani 
			business communities is convinced that an FTA is a good idea, so 
			there’s some hesitation. In the United States we also say, “He who 
			hesitates is lost.”<br>
			<b><br>
			FTA to boost investment </b><br>
			I’m sharing some views on why I think that the private sector should 
			embrace the FTA and work to realise its full potential. After all, 
			the FTA is really a framework, or a platform, and we will be 
			squandering a rare opportunity if we–the business community–don’t 
			make this FTA work for our benefit.<br>
			<br>
			For nearly 40 years, the National US-Arab Chamber of Commerce (NUSACC) 
			has been a pre-eminent organisation for fostering trade and 
			investment between the United States and the 22 countries of the 
			Arab world. For Omani companies looking for business expansion, 
			NUSACC is uniquely positioned to provide services in the US market. 
			These include such basic business services as due diligence, 
			matchmaking, sourcing, certification, and the like. <br>
			<br>
			Arab investors are devoting more money to this region than ever 
			before. Part of this trend, of course, can be attributed to high oil 
			prices and the after effects of September 11–notably, when some Arab 
			business leaders redirected their investments away from the United 
			States and ploughed them into projects closer home.<br>
			<br>
			But there is an even more important factor at work here: confidence. 
			With the economic and regulatory reforms that have been taking place 
			in the Middle East in recent years, Arab business leaders today have 
			more confidence than ever that their investments – in technology, 
			capital projects, infrastructure and, especially, people – will grow 
			and will be protected. And when local business leaders invest in the 
			Arab world, it sends a very positive signal to other investors 
			worldwide.<br>
			<br>
			So what does this have to do with the FTA? Precisely this: In 
			signing an FTA with the United States, Oman is sending a strong 
			signal to traders and investors around the globe that the Sultanate 
			has met and surpassed the “gold standard”–America’s rigorous 
			economic and regulatory requirements. The FTA lends additional 
			credibility and helps to position Oman as one of the primary 
			beneficiaries of the regional confidence.<br>
			<b><br>
			Solid credentials </b><br>
			But don’t take my word for it. Recently, I attended the Arab 
			Strategy Forum in Dubai, where they released the findings of the 
			Second Arab Business Intelligence Report. Out of the 22 nations in 
			the Arab world, Oman ranked number three on the list of nations that 
			inspire the most economic confidence–just slightly behind Saudi 
			Arabia and Qatar.<br>
			<br>
			Still not convinced? Then let’s take a look at the numbers which 
			NUSACC is releasing through OER. In the first nine months of 2006, 
			through the end of September, Oman’s bilateral trade with the United 
			States (US$ 1.258 billion) surpassed the total amount for the year 
			2005 (US$ 1.149 billion). By the end of this year, US exports to 
			Oman are expected to reach US$ 747.2 million, while Omani exports to 
			the United States are on track to reach US$ 935.6 million, totalling 
			US$ 1.683 billion for the year. This represents an increase of more 
			than 45 per cent in the past 12 months.<br>
			<br>
			Of course, there are many reasons for this increase in bilateral 
			trade, but one of the main reasons is what our Chamber calls the 
			‘FTA Effect’. The FTA Effect is increased trade with the United 
			States that is driven, in part, by the perception that Oman and the 
			other FTA nations are strengthening their trade and investment 
			regimes to attract more business with the United States.<br>
			<br>
			For example, among the four Arab nations that have made the most 
			headway in carrying out FTAs with the United States–Jordan, Morocco, 
			Bahrain and Oman–bilateral trade jumped 16.3 per cent from 2004 to 
			2005 and an estimated 33.5 per cent from 2005 to 2006. In other 
			words, bilateral trade with the US roughly tripled during the first 
			period and then more than doubled in the past year – surpassing non-FTA 
			nations, which registered an increase of 29.2 per cent.<br>
			<br>
			The FTA Effect also seems to have a stabilising, or smoothening, 
			effect on trade variances. US bilateral trade with non-FTA nations 
			has been somewhat erratic, while US bilateral trade with FTA nations 
			has been consistent and less susceptible to peaks and valleys. What 
			the FTA Effect boils down to, then, is confidence: the perception 
			that these nations, in signing FTAs with the United States, are 
			holding themselves to a higher trade and investment standard than 
			ever before.<br>
			<b><br>
			Short-term challenges, long-term gains</b><br>
			Free Trade Agreements are not really “free”, of course. Most FTAs 
			come with a price tag, and the Oman-US FTA is no exception. What 
			might be the costs for the Sultanate of Oman?<br>
			<br>
			In the short run, it may cost some jobs as Omani businesses 
			reposition themselves to be more competitive in the international 
			marketplace. The FTA may also shake up the “pecking order” among 
			Omani companies when, for the first time, small and medium-sized 
			enterprises (SMEs) will have as much opportunity as Oman’s largest 
			firms to penetrate the US market.<br>
			<br>
			There may be some short-term challenges associated with the Oman-US 
			FTA. But from the perspective of NUSACC, the benefits of this new 
			agreement clearly outweigh the liabilities. First, the FTA will 
			provide Omani companies with preferential access to the US market. 
			That’s 300 million consumers in a nation that imported approximately 
			US$ 2 trillion worth of goods and services this year. Access to the 
			US market will be particularly appealing to Oman’s ‘downstream’ 
			derivatives, which will be well positioned to stoke US industry.<br>
			<br>
			Second, signing the FTA sends a message to the international 
			marketplace that Oman is committed to some of the most 
			investor-friendly policies in the region. This is important at a 
			time when competition for investment funds has become truly global 
			and the Sultanate is competing against the best in the world.<br>
			<br>
			Third, American companies make first-rate partners, and Omani firms 
			will be better positioned than ever to tap into US experience and 
			expertise. This certainly applies to such traditional sectors as 
			engineering and construction, defence, and consumer items. But even 
			more importantly for Oman’s service-oriented economy, perhaps, the 
			FTA should help to open the door for partnerships in such leading 
			edge sectors as Information &amp; Communications Technology (ICT), 
			education and training, financial services, insurance, hospitality 
			and leisure, and logistics.<br>
			<br>
			The FTA between Oman and the United States has created ‘win-win’ 
			opportunities. It will help both sides to increase exports, generate 
			jobs, encourage investment, develop business practices that are well 
			suited to the international marketplace and, over time, improve the 
			quality of life in the United States and the Sultanate.<br>
			<br>
			I started this article with an American saying, and I will end my 
			remarks with an Arab saying: “One hand alone cannot clap”. It’s all 
			about partnerships. The public and the private sector must work 
			hand-in-hand to seize the initiative and maximise the many benefits 
			that the US-Oman FTA has to offer in 2007 and beyond.</font></p>
