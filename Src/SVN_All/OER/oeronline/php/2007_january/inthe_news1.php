<p align="justify" style="margin-left: 5; margin-right: 5">
<font face="Verdana" size="1">
<b>In 2000, ECGA of Oman became the first Export Credit Agency in the Arab World 
and the Africa/Middle East Region to become a member of the Prague Club. Six 
years later, Muscat hosted the first meeting of the Club in the Middle East and 
Africa region, reports Sunil Kumar Singh</b></font></p>
