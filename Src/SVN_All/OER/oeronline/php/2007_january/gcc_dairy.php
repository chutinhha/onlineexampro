		<p align="justify"><font face="Verdana" style="font-size: 14pt"><br>
        <b>Dreaming big!</b></font><font size="1" face="Verdana"><b><br>
		Buoyed by the recent successful sporting activities, GCC countries are 
		aiming to host much bigger events now. The potential is definitely 
		immense, reports Sowmya Sri Rallabhandi</b></font></p>
		<p align="center">
		<img border="0" src="gcc_dairy.jpg" width="400" height="254"></p>
		<p align="justify"><font size="1" face="Verdana">After giving immense 
		importance to the construction sector, the Gulf region is now looking 
		beyond to other sectors, primarily to sports. The sports sector, along 
		with tourism, has become a thriving industry in the region. In fact, GCC 
		countries led by UAE are hosting a number of international sports events 
		in the Middle East region. <br>
		<br>
		UAE is already holding major international events like Dubai Duty Free 
		Tennis Open, Dubai Desert Classic, in a wide range of sports, from horse 
		racing to motor racing, golf, tennis, football, rugby, cricket, sailing, 
		powerboat racing and almost every other competitive sport. Joining it is 
		Qatar that hosted the 15th Asian Games, first time in the Gulf region 
		with a budget outlay of US$ 2.8 billion. Bahrain too is showing 
		tremendous interest and has allocated a high budget for the development 
		of sports and youth activities. In 2005, Bahrain hosted six world-class 
		sports championships, including Grand Prix Formula 1 motor races. Other 
		major sporting events hosted in the region include Qatar Tennis 
		Federation’s Qatar Exxon Mobil Open for men and Qatar TotalFinaElf Open 
		for women with an investment of US$ 1.5 million. International Rugby 
		Board Sevens and the world’s richest horse race – Dubai’s Godolphin 
		stable – now dominate the international horse racing around the world.
		<br>
		<br>
		These sports events are expected not only to benefit the construction, 
		hospitality, travel and tourism sectors but also help in overall 
		economic development of the respective countries.<br>
		<br>
		</font>
		<img border="0" src="gcc_dairy1.jpg" width="150" height="119" align="left" hspace="5" vspace="2"><font size="1" face="Verdana">International 
		sports federations are also looking at the Gulf region to host for 
		hosting their events due to vast infrastructure facilities and region’s 
		enthusiasm to promote sports activities. The International Cricket 
		Council has shifted its headquarters from London to Dubai to reap the 
		benefits of non-tax reputation of Gulf. In 2003, International 
		Federation of Motorcycles (FIM) chose UAE as the venue for its World 
		Champions’ Awards ceremony – held for the first time outside the 
		traditional European venue of Monte Carlo. The 1999 Bowling World Cup 
		Championship and the 2003 FIFA Youth World Cup held in the UAE are just 
		few of the many international sporting events held in the region.<br>
		<br>
		Buoyed by these successful sporting events, GCC countries are aiming to 
		host bigger events now. Both Dubai and Qatar are bidding to host the 
		2016 Olympic Games. Dubai is also bidding for World Athletics 
		Championship in 2011 and World Cup for Beach Football in 2007, while 
		Qatar is eying the 2015 Asian Cup Football.<br>
		<b><br>
		World Class Facilities</b><br>
		To meet international standards, the countries are upgrading their 
		sporting facilities with huge investments. While UAE is building a motor 
		sports stadium, Dubai is working on an Autodrome and Business Park, 
		worth US$ 18 billion as per the FIA standards. As part of the US$ 
		5.7-billion Dubailand project, it is constructing a US$ 2-billion Sports 
		City, due to open in 2007. Covering an area of 4.65 million sq. m., 
		Sports City will be the first integrated purpose-built venue in the 
		world, incorporating a Manchester United soccer school (the first 
		outside Europe), a championship golf course designed by Ernie Els, David 
		Lyons tennis stadium, and Butch Harmon golf academy among other 
		facilities.<br>
		<br>
		Qatar spent US$ 2.8 billion on preparing venues for the Asian Games, 
		including upgrading the seating capacity of the Khalifa Stadium to 
		50,000 seats and the construction of an indoor sports complex, the 
		world’s largest indoor multi-sports dome.<br>
		<br>
		The increase in sporting activity has also led to a rise in sports 
		sponsorships. Airline majors are leading the corporate world in this. 
		Emirates Airline, Qatar Airways, Emmar Properties, Shell, Marriott 
		Hotels, BMW, Marlboro, Coca-Cola, Nike, Yas Perfumes, LaCoste, etc., all 
		are eyeing high growth opportunities through sports. Interestingly, Gulf 
		companies are not only sponsoring local events but also going global to 
		get a greater worldwide presence.<br>
		<b><br>
		Sponsors Line Up</b><br>
		Emirates Airlines has become the official partner of FIFA from 2007 to 
		2014. It is also a key sponsor of Arsenal football club. Its sponsorship 
		deals include the Asian Football Confederation, that will run till 2008; 
		a multi-million pounds three-year deal with Referees, the Professional 
		Game Match Official Limited (PGMOL), which Emirates announced in August 
		2004; a three-year official partnership deal with Paris St. Germain, the 
		French football club; and sponsorship of the Australian Cricket team for 
		the next three years.<br>
		<br>
		Emirates already sponsors Collingwood FC, the country’s most 
		widely-supported Aussie Rules football team; Emirates Western Force, the 
		Super 14 rugby team and various horse-racing events, including the 
		Emirates Melbourne Cup, the biggest horse race in the country.<br>
		<br>
		Similarly, Qatar Airways is not just the official airline for the 15th 
		Asian Games, but is also the official airline sponsor to English Test 
		Match Grounds like Chester-le-Street, Edgbaston, Headingley, Lord’s, Old 
		Trafford, Trent Bridge and The Brit Oval. The Airways also sponsors 
		CNN’s new ‘Quest’ and the Weather Forecast on BBC World. Etihad Airways 
		and Qatar Airways narrowly missed the opportunity of sponsoring 
		Manchester United and other European football clubs.<br>
		<br>
		Qatar National Bank has entered into an agreement worth QR 25 million 
		with Qatar National Olympic Committee for the next three years, to 
		sponsor major sporting events in the Qatar such as football, basketball, 
		volleyball and handball and also the QNB Losail Motorbike Race and the 
		Qatari Middle East Rally Champion. Similarly, Standard Chartered is the 
		title sponsor of the Dubai Marathon for the next three years.<br>
		<br>
		HSBC has teamed up with Dubai Autodrome to form the HSBC Racing Academy 
		that will provide an advanced motor racing training programme and three 
		scholarship places at the La Filliere Racing School in France for young 
		UAE racing drivers.<br>
		<br>
		The list goes on and on. Sports development is still at a primary stage 
		in the Gulf region but the governments and corporates have realised 
		their potential. Sports activities contribute to generation of jobs via 
		sport tourism, marketing and sporting events. If the corporate world and 
		local governments work in tandem, the day is not far when Gulf region 
		will become a major sports hub in the world.</font></p>
		<p align="justify"><font size="1" face="Verdana"><b>
		<img border="0" src="gcc_dairy2.jpg" width="219" height="387" align="left" hspace="5" vspace="2">2005 GDP grows 6.5%</b><br>
		The free economy policies adopted by the Kingdom of Saudi Arabia have 
		accelerated economic growth in all its sectors during 2005. It reflects 
		the positive conditions in 
		the world oil market, state of continued optimism and improvement in the 
		domestic investment environment in all sectors of the economy, says the 
		Saudi Arabian Monetary Agency (SAMA).<br>
		<br>
		According to a recent SAMA report, the GDP growth rate (at constant 
		prices) increased by 6.5 per cent in 2005, compared to 5.3 per cent the 
		preceding year. The state budget registered a substantial surplus of US$ 
		57.93 billion, which was equal to 18.8 per cent of the nominal GDP. The 
		balance of payments current account recorded a surplus for the seventh 
		consecutive year, standing at US$ 90.44 billion with an increase of 74.6 
		per cent over the preceding year, or about 29.3 per cent of total GDP.<br>
		<br>
		The state of optimism in 2005 continued in the Kingdom’<b><img border="0" src="gcc_dairy3.jpg" width="180" height="231" align="right" hspace="5" vspace="2"></b>s investment 
		environment, supported by economic growth, low inflation rate and an 
		increase in profitability of national joint-stock companies, which 
		enhanced the uprising trend of the general share price index in 2005. 
		The domestic general share price index rose substantially by 103.7 per 
		cent, SAMA reported.<br>
		<br>
		The economic reforms adopted by the Kingdom, it said, significantly 
		enhanced the role of private sector in development process. The 
		contribution of the private sector to the aggregate GDP (at constant 
		prices) stood at 44.6 per cent during 2005, and its contribution to the 
		non-oil GDP (at constant prices) amounted to 65.9 per cent. <br>
		<b><br>
		Oil prices decline</b><br>
		Kuwait’s oil price continued its decline to below US$ 55 per barrel for 
		the tenth consecutive week.<br>
		Source: AlShall report<br>
		<b><br>
		Marginal earnings</b><br>
		<b>
		<img border="0" src="gcc_dairy4.jpg" width="100" height="109" align="left" hspace="5" vspace="2"></b>Most of the UAE companies reported modest growth earnings during the 
		first nine months of 2006. However, a closer look shows that many small 
		companies performed poorly, leading to a weak performance in the overall 
		market. If the loss-making companies, that represent only 2 per cent of 
		the total market capitalisation, were ignored then the UAE companies 
		would have an earnings growth of around 11 per cent in the first nine 
		months of 2006.<br>
		<br>
		According to a study conducted by the National Bank of Kuwait, the UAE 
		markets witnessed marginal earnings growth of 5 per cent in the first 
		nine months of 2006, compared to the same period last year. The service 
		sector was the only one that witnessed a double-digit growth, while 
		insurance, along with industry and hotel, witnessed significant declines 
		in earnings. Though the banks saw a growth in their income from core 
		operations, a majority of them witnessed a significant drop in their 
		investment income, thus resulting in a meagre 3 per cent growth in 
		earnings.<br>
		<br>
		In the first nine months of 2006, in a sample of 66 companies, 14 
		leading companies representing 60 per cent of total market 
		capitalisation had a combined net income of US$ 5,028 million, a growth 
		of 27 per cent from the same period last year. The remaining companies 
		of the sample witnessed a drop of 27 per cent in the earnings compared 
		to same period in 2005.<br>
		<b><br>
		Dubai’s exports to GCC up 49%</b><br>
		Exports of Dubai to GCC have jumped by 49 per cent to hit US$ 5.22 
		billion in 2005, from US $3.5 billion a year earlier. Imports also 
		increased, though relatively slower, by 11 per cent to reach US$ 1.06 
		billion. The largest export market of Dubai was Saudi Arabia with 45 per 
		cent of total exports to GCC. On the other hand, Oman was the smallest, 
		with only 6 per cent exports. Kuwait accounted for 24 per cent of 
		exports, Qatar 14 per cent and Bahrain 12 per cent. The GCC, however, 
		has not been a major source of imports of Dubai. Trade surplus from 
		trade with GCC amounted to US$ 4.16 billion, for an export-import ratio 
		of about 5:1. GCC countries accounted for only 1.4 per cent of Dubai’s 
		total imports. Again, Saudi Arabia was the biggest supplier, with 54 per 
		cent of the total supplied by the country.<br>
		<b><br>
		Gulf, losing shine!</b><br>
		<b>
		<img border="0" src="gcc_dairy7.jpg" align="right" hspace="5" vspace="2"></b>Despite one of the highest living costs in the region and salary levels 
		that are below Qatar and Saudi Arabia, the UAE remains the Gulf’s most 
		attractive destination for expatriate professionals, according to the 
		latest research by GulfTalent.com, an online recruitment firm.<br>
		The report, Pay, Inflation and Mobility in the Gulf, analysed cost of 
		living and employment patterns in the Gulf. According to the survey, 73 
		per cent of expatriates relocating to the Gulf prefer to work in the UAE, 
		while 78 per cent of the country’s current expatriate residents plan to 
		remain there, the highest proportion in the Gulf.<br>
		Source: GulfTalent.com<br>
		<br>
		The survey found that Qatar and the UAE are experiencing an all-time 
		high in inflation, particularly in housing. Average accommodation 
		rentals increased by a total of 83 per cent in Doha over the last two 
		years and 60 per cent in Dubai, compared to just 21 per cent in Riyadh. 
		An average two-bedroom apartment in Dubai now costs US$ 1,850 per month, 
		compared to US$ 680 in Riyadh.<br>
		<br>
		According to the report, rent as a proportion of total household income 
		has reached 33 per cent in Qatar and 30 per cent in the UAE, compared to 
		19 per cent in Saudi Arabia, the lowest in the region. Saving rates in 
		the UAE are now the lowest in the region, with 43 per cent of 
		expatriates surveyed reporting zero or negative saving rates due to high 
		costs and abundance of spending opportunities.<br>
		Source: GulfTalent.com<br>
		<br>
		Overall, the findings suggest that the financial attraction of the Gulf 
		as a place for high savings is rapidly diminishing – particularly in the 
		case of the UAE. This has made it more difficult to attract expatriate 
		professionals, particularly from traditional sources such as India and 
		Jordan which are experiencing rapid economic growth of their own. As a 
		result, many expatriates are returning to their home countries.<br>
		<br>
		A similar situation is also seen with European expatriates. With Gulf 
		currencies pegged to the US dollar, the weakness of the US currency has 
		sharply reduced the attractiveness of Gulf salaries in Euro and Sterling 
		terms as well.<br>
		- Sowmya Sri Rallabhandi<b><br>
		<br>
		Privatisation is order of the day </b><br>
		<b>
		<img border="0" src="gcc_dairy6.jpg" width="150" height="116" align="left" hspace="5" vspace="2"></b>Bahrain’s economy is heading for promising prospects in 2007 riding on 
		the expanded role of private sector firms in day-to-day management of 
		economic activities. There is growing appreciation of private sector 
		establishments, not least because of their willingness to invest 
		relatively large amount of funds besides taking responsibility for 
		shortcomings.<br>
		For good reasons, this newfound determination is not confined to 
		Bahraini firms, as evidenced by the overtures extended to foreign firms. 
		International firms are now in control of two strategic assets, namely 
		production of electricity and management and operation of port services.<br>
		<br>
		In 2006, a consortium comprising of International Power (IP) of the UK, 
		Sumitomo Corporation of Japan and Suez Tractebel of Belgium was awarded 
		a contract to take over operation of the brownfield power and water 
		project at Hidd on the northern island of Muharraq. Hidd Power Company (HPC) 
		is owned 40 per cent by IP with of Sumitomo Corporation and Suez 
		Tractebel owning 30 per cent each. The job involves acquisition of the 
		existing 1,000 MW and 30m gallon per day (g/d) complex. Additionally, 
		HPC must install new capacity of 60m g/d, which would significantly 
		expand the amount of desalinated water. The US$ 1.25 billion water plant 
		will be operated on build-own-operate (BOO) basis. Privatisation of 
		Hidd’s utility complex is the largest of its kind in Bahrain till date.<br>
		<br>
		In another move, the authorities have outsourced management and 
		operation of the country’s two ports. APM Terminals International has a 
		25-year-concession contract (starting from December 2006) to operate 
		Mina Salman plus Khalifa bin Salman port. Mina Salman is in Manama and 
		currently serves as Bahrain’s main port. Located at Hidd, Khalifa bin 
		Salman port is still under construction, and should be operational by 
		2008. The authorities believe that privatisation of Bahrain’s ports 
		should result in savings of US$ 928 million over the coming 25 years due 
		to higher efficiency and drop in transportation costs. Maersk A/S, in 
		turn fully owned by the Danish A P Moller-Maersk, owns 80 per cent of 
		APM Terminals International with the local Yusuf bin Ahmed Kanoo 
		Holdings owning the balance 20 per cent. <br>
		<br>
		Earlier, the authorities had privatised public transport and 
		municipality services. Other planned privatisation efforts include 
		governmental postal services.<br>
		<b><br>
		Greater efficiency</b><br>
		Needless to say, Bahrain’s economy is bound to become only more 
		efficient, as private sector firms increasingly play a leading role in 
		providing vital services like ports and electricity. It may be recalled 
		that in August 2004, the whole of Bahrain was hit by a blackout due to 
		management problems at Hidd power plant. Still, the authorities avoided 
		taking direct responsibility and thus refused to provide any sort of 
		compensation to any party. However, no repeat of that “Black Monday” is 
		envisaged with established international firms assuming control of power 
		production. Still, unlike the government, the consortium could be held 
		accountable in case of any future mishaps. Not surprisingly, the 
		government has promised to entice private investors to develop all 
		future power plants.<br>
		<br>
		In short, involvement of private investors regardless of nationality in 
		the management of power, water and ports suggests that the authorities 
		are looking for efficient and effective use of the country’s limited 
		resources.<br>
		<br>
		- Dr Jasim Husain Ali, Head-Economic Research Unit, University of 
		Bahrain &amp; MP</font></p></td>
