                    Cover Story<br>
                    </font><font face="Verdana" size="4" color="#FF0000">EVENT 
            MANAGEMENT<br>
            </font><font face="Verdana" color="#FF0000" size="2">Hope, Hype or 
            Hoopla?</font><p align="justify"><font face="Verdana" size="1"><b>
            <img border="0" src="im21.jpg" align="left" hspace="5" width="250" height="258">Event 
            management as an industry continues to grow exponentially, with 
            increasing number of companies coming forward to organise and 
            sponsor events, shows and programmes. At last, event management has 
            come to be recognised as an industry by companies and individuals 
            alike. But when can you expect this nascent industry to come of age 
            and attain global class? Vicky Kapur reports </b><br>
            <br>
            It was as inconspicuous as any other fax missive lying on my always 
            cluttered table, one working day in October last year. On closer 
            scrutiny, however, I found that the message was an invite to attend 
            a function organised by Momentum PR - a company floated by Sarah 
            Livingston of Oman TradaNet fame - to celebrate the launch of Oman 
            Oil Marketing Co., the new avatar of the erstwhile BP Oman. <br>
            <br>
            Rebranding of a company is nothing new. Neither is change of 
            ownership/management in an organisation. Nor is the announcement of 
            the same by a corporate PR firm. What is new, however, is the trend 
            of self-styled 'communications companies' to venture into the arena 
            of event management, a task hitherto handled by a cluster of 
            specialist agencies in Oman. <br>
            <br>
            Even National Publishing &amp; Advertising (NPA), which had burnt its 
            fingers in the early 1990s when it organised a couple of shows and 
            made huge losses, made a comeback of sorts in this segment by 
            organising two successful entartainment shows targeted at the Indian 
            expatriate community in quick succession in November and December.
            <br>
            <br>
            Earlier, in late September, Impact Films, the event management arm 
            of Advertising International (Adinc), had already announced its 
            entry into the big-ticket commercial events segment by hosting World 
            Fashion, a grand fashion show that brought the 'hottest of haute 
            couture' from around the world to Oman. Adinc also has been 
            successfully hosting the coveted Oman Awards for Excellence, a 
            premiere annual business event, since 1999, when they were first 
            chosen to handle it. <br>
            <br>
            While specialist event management companies like Intevents and 
            Omanexpo have been around for a while in Oman, the entry of the 
            likes of Momentum PR - a dedicated PR agency that undertakes event 
            management for its clients; NPA - an ad agency that also holds a 
            licence to host events; Gazelle - a three-and-a-half-year-old 
            product promotion and sampling firm which has begun organising 
            entertainment events since the past six to eight months; Ideas - a 
            management consultancy and training organisation which is into 
            organising workshops and symposiums; and Darran LLC - an event 
            management company specialising in managing conferences, incentives, 
            special events, product launches, road shows and exhibitions, is a 
            new trend. <br>
            <br>
            <b>Why events? </b><br>
            So why is every PR / communications / ad agency worth its salt 
            rushing into organising and managing events? Is there really a big 
            business potential in the industry in Oman? The answer to that is an 
            astounding yes! Due to the nature of Oman's population - about a 
            quarter of the Sultanate's residents are expatriates - the number of 
            international performers visiting the country for participating in 
            entertainment events is on the rise. Big and small companies in Oman 
            also view such events as means of reaching out to selected audience 
            and promote their products. <br>
            <br>
            With increasing globalisation and the mounting popularity of 
            satellite television, the awareness and interest in international 
            and domestic artists has got enhanced, resulting in rapid growth in 
            live entertainment and event management industry. There has been a 
            flurry of activity, and event management agencies are mushrooming. 
            This novel way of packaging entertainment has its roots firmly 
            embedded in advertising and PR.<br>
            <br>
            Corporate houses consider sponsoring events featuring popular 
            artists as a means of reaching their target consumers. Therefore, 
            corporate sponsorship of events to promote products has also seen a 
            big spurt. A recent trend amongst large organisations, especially 
            FMCG and automobile companies, is to associate their brands with 
            quality entertainment shows. <br>
            <br>
            Quick, back-of-the-envelope calculations by experts OER spoke to 
            reveal that the event management industry - including social, 
            business, fashion, entertainment, sports and corporate events - has 
            the potential to gross more than RO10 million in five years from 
            now. Industry experts put the current value of the event management 
            business, which was estimated at only RO200,000 in the late 1990s, 
            anywhere between RO1 million and RO3 million. That's evidence enough 
            of the exponential growth that the industry is witnessing. <br>
            <br>
            Reasons Nicholas Hodge, CEO, Intevents, the event management arm of 
            Oman International Trade &amp; Exhibitions: &quot;A lot of large 
            organisations and certain government departments are currently 
            managing their events through in-house resources. However, as these 
            organisations slim down to become more focused on their 
            competencies, they'll realise the benefits of outsourcing it to 
            specialists.&quot; The event management industry continues to grow 
            exponentially, with increasing number of companies coming forward to 
            sponsor events, shows and programmes. Points out Rajesh K. Raman, 
            general manager, Adinc: &quot;Event management as a business isn't just 
            growing - it's exploding. It's really growing exponentially because 
            a lot more companies are beginning to realise the value of events 
            and a lot of new companies are sponsoring events.&quot; <br>
            <br>
            Says Ashok Suvarna, general manager, NPA and general manager 
            (advertising), Times of Oman: &quot;Event management has come to be 
            recognised as an industry by companies and individuals alike. 
            Earlier, one saw hardly one or two major events a year in Oman. Now, 
            you see a big one happening almost every month.&quot; Agrees Sarah 
            Livingston, managing director, Momentum PR: &quot;Of late, there have 
            been a lot of new players coming in. The number of organisations in 
            Oman that are appreciating the value of events and what they can 
            actually do for their businesses is certainly growing.&quot; A sentiment 
            echoed by Dr Sunil Gupta, chief learning officer, Ideas: &quot;The event 
            management business in Oman is evolving at a fast pace and the room 
            for growth in certain niche segments is tremendous.&quot; <br>
            <br>
            Counters Rajeev Chowdhary, the man responsible for organising events 
            hosted by Gazelle: &quot;Looking at the recent successes of some of the 
            big-ticket events, more companies will definitely come in. However, 
            currently, there is no space for more companies.&quot; Agrees Amit Sampat, 
            director, Gazelle: &quot;The Oman market is small, and there is perhaps 
            scope for two big events a month, which are already happening now.&quot;<br>
            <br>
            Besides, as Chowdhary puts it, &quot;The entry of fly-by-night event 
            managers affects the entire market as sponsors who have burnt their 
            fingers with such organisers shy away from sponsoring events with 
            other professional set-ups as well.&quot; However, players like NPA's 
            Suvarna remain optimistic. &quot;There is definitely room for more 
            companies. The only pre-requisite is that they should organise 
            events professionally. Competition will obviously raise the quality 
            bar.&quot; <br>
            <br>
            While specialist organisations like Intevents, Omanexpo and Darren 
            LLC establish a particular event and then perhaps look for sponsors, 
            most of the new companies entering the arena are undertaking events 
            as part of a client servicing exercise. &quot;In our endeavour to offer 
            the full spectrum of services to our clients, we began by 
            undertaking management of certain events like road shows for them,&quot; 
            maintains Adinc's Raman. &quot;We then went to the next level by handling 
            corporate events like the Oman Awards for Excellence, which has 
            earned us a lot of goodwill on our skills.&quot; <br>
            <br>
            &quot;Combining this and the fact that we have a full production unit, we 
            felt that we were uniquely placed by the virtue of the services we 
            have to handle corporate events. And now we've extended that into 
            commercial events. The reason we started undertaking commercial 
            events was to offer our clients greater opportunities to promote 
            their brands,&quot; he explains. &quot;Events are a useful communication tool. 
            However, they are best and most effectively used in conjunction with 
            other tools,&quot; adds Momentum's Sarah. <br>
            <br>
            <b>What's in it for the sponsors? </b><br>
            So are businesses in Oman going to abandon the traditional modes of 
            advertising a.k.a. print and electronic media and jump on to the 
            events bandwagon? Of course not. Industry players are unanimous that 
            promoting brands through events is definitely not a replacement for 
            the print and the electronic medium. They can at best supplement the 
            two streams. Explains Sarah: &quot;Since a lot of people don't need to 
            reach the entire population of Oman, it isn't always advisable to 
            deploy mass media as, for most organisations, it can be very 
            expensive and not particularly effective in certain cases.&quot;<br>
            <br>
            &quot;Although you do have certain targeted publications, they aren't 
            huge in numbers as compared to somewhere like Dubai or Europe. So, 
            you don't have very effective means of targeting a specific 
            audience. Through events, you can directly target who you want to 
            reach,&quot; she adds. Justifies Adinc's Raman: &quot;An event scores over 
            other forms of advertising in certain cases where, for example, you 
            want to address a limited, niche audience or you want to be 
            associated with a particular event. You can't generalise and say 
            that one is better than the other. Like PR or mass media 
            advertising, it's just one more choice in the armoury of an 
            advertiser in fulfilling his objective.&quot; As NPA's Suvarna puts it 
            rather succinctly: &quot;Brand-building, top-of-mind recall and a focused 
            target audience are something that only an event can offer.&quot; <br>
            <br>
            Claims Dr Gupta of Ideas: &quot;The sponsors, of course, benefit from the 
            publicity in terms of visibility in banners, advertising and 
            brochures of the event. I don't think it adds value in terms of 
            short-term product sales, but definitely provides visibility.&quot; 
            Concurs Intevents' Hodge: &quot;If a company sponsors an event, one of 
            the benefits they get is proactive interaction with the target 
            audience. The options through advertising tend to be quite passive, 
            but events offer interactivity.&quot; <br>
            <br>
            Events are basically an extended form of advertising, allowing 
            brands to associate directly with the consumer. &quot;Events certainly 
            offer the sponsors a focused set of audience and a company would 
            want its customers to associate it with quality shows and events,&quot; 
            offers Gazelle's Chowdhary. &quot;In fact,&quot; adds Gazelle's Sampat, &quot;the 
            response from sponsors, who receive mileage based on the level of 
            sponsorship, has been so good that we have planned one big 
            entertainment event almost every month for the next year or so.&quot;<br>
            <br>
            Moreover, you also can be a lot more flexible - placing an advert or 
            sending somebody a flyer or a brochure has limited appeal and can 
            only disseminate certain amount of information. The emotions that 
            can be generated through an event, on the other hand, can be a lot 
            more powerful and dynamic. As Momentum's Sarah puts it, &quot;Events 
            aren't always targeted at external stakeholders. A lot of companies 
            organise motivational events internally for their staff as well.&quot; 
            Adds Gazelle's Chowdhary: &quot;The sponsors distribute tickets/passes 
            for the events among their staff to keep them inspired.&quot;<br>
            <br>
            Besides, brand building is an important objective that can be met by 
            events. &quot;While we believe in events for our clients - direct 
            customer contact is something that is desired by many of our clients 
            because it offers opportunity for touch-and-feel and segmented 
            marketing - dissemination of detailed information and brand-building 
            by association are the natural benefits of an event,&quot; believes 
            Adinc's Raman. &quot;Sometimes, you may not want people to run away and 
            buy whatever you may be selling, but you may be trying to change a 
            perception and generate or instil a certain feeling in a certain 
            type of audience. That's where events come handy,&quot; claims Sarah. <br>
            <br>
            Then there is the obvious objective of launching a new product or 
            service with a bang so that people remember the event and, thus, the 
            new offering. Besides these, quiz programmes like the ones hosted by 
            Times of Oman and the Antakshari, hosted by NPA in association with 
            the Indian Social Club, are a big hit with niche audiences. On a 
            non-commercial note, the genre of events organised by social and 
            charitable organisations and ministries are meant purely for the 
            entertainment and well-being of the general population.<br>
            <br>
            <b>The dynamics </b><br>
            Event management companies involve themselves in the planning, 
            organising and execution of an event, which could include a product 
            or a brand launch, an exhibition, a concert or even a conference. 
            Whatever the event may be, experts agree that it's the execution 
            that holds the key to success. &quot;While having a strong plan is the 
            prerequisite, the secret behind a good, successful event lies in its 
            execution and proper detailing - carrying things to a plan,&quot; 
            emphasises Adinc's Raman. <br>
            <br>
            &quot;Attention to detail is one of the most important things for an 
            event to be successful. You could have the best idea for an event in 
            the world, which could be the most creative, exciting, dynamic idea, 
            which on paper and your head looks absolutely fantastic, but if the 
            logistics have not been worked out properly and it is not practical 
            to implement, then it can turn out to be in absolute shambles,&quot; 
            argues Momentum's Sarah. &quot;Another way to look at it is - lists, 
            lists, and more lists,&quot; avers Michel Crawley, director (corporate 
            events), Intevents. <br>
            <br>
            Explains Ideas' Gupta: &quot;There is a lot of planning and management 
            skills required to host any kind of an event. Right from the 
            marketing of an event to coordination of invitees, speakers, 
            finalising the venue, seating arrangement, addressing regulatory 
            issues… it involves a lot of work.&quot; Cautions Intevents' Hodge: 
            &quot;Event management is a tough business. It isn't an easy thing to do. 
            It will be foolish to believe that anybody can do it. You can easily 
            get your fingers burnt if you are not careful or if you don't have 
            the necessary skill-sets or experience.&quot; Like what NPA's Suvarna did 
            when he entered the segment without proper ground work. &quot;Frankly, 
            when we first entered the field in the early 1990s, we did no 
            research and the planning was bad as well. Now, however, we realise 
            the importance of research and are committed to deliver the desired 
            results for our sponsors as well as the audience.&quot;<br>
            <br>
            Affirms Gazelle's Sampat: &quot;A lot of new players are one-time wonders 
            - people who get into event management for the sake of organising 
            events and end up with their fingers burnt. When we decided to get 
            into the live entertainment business, we did our homework well and 
            thought about every little detail including having the seats 
            numbered and ushers to escort people to their respective tables.&quot; 
            Adds Momentum's Sarah: &quot;Planning is incredibly important. It's very 
            easy to miss out on certain things - contingency plans are always 
            good. There are always going to be things that come up, there are 
            always going to be little unforeseen circumstances - so effective 
            planning is very important.&quot; Confirms Intevents' Hodge: &quot;Basically, 
            it's planning, attention to detail, and the stamina to keep pushing 
            at it.&quot; <br>
            <br>
            Apart from the basics, it is equally important to be different in 
            you approach. &quot;Leading business people in Muscat would be getting 
            invitations to attend five different functions every week. There are 
            a host of events that go on in Muscat - the problem is that very few 
            are remembered for being anything outstanding,&quot; laments Momentum's 
            Sarah. <br>
            <br>
            &quot;What we think is important is to try and do something different. 
            Whether it is through the layout, whether it's through the 
            entertainment that you bring in, whether it's through the set up, 
            however you do it, it is just bringing that little bit something 
            extra that can make the event memorable. It can be done through 
            simple things like invitations. One should tend to think outside the 
            box instead of using the same ideas and same things over and over 
            again,&quot; she adds. <br>
            <br>
            Agrees Adinc's Raman: &quot;The World Fashion show was extremely well 
            executed. It wasn't that the idea was particularly unique. Sure, it 
            had a novelty value as well, but the point is that it worked well 
            because it was executed well.&quot; He adds: &quot;We have been approached by 
            several organisations to undertake not just fashion shows but events 
            that are sophisticated in nature as opposed to the regular 
            run-of-the-mill events that people do.&quot; As NPA's Suvarna points out, 
            &quot;The audience will only appreciate what you are doing if you do 
            something unique and different from the run-of-the-mill events 
            happening everywhere.&quot; <br>
            <br>
            But how different can you be in a conservative market like Oman? 
            &quot;Sometimes, people are wary of what they can and can't do because 
            they think that the market is not ready for it. I think they are 
            over-conservative at times when, in fact, you can do things that are 
            by no means controversial but that do go down incredibly well 
            because people like to see something different,&quot; opines Sarah.<br>
            <br>
            &quot;The Oman Oil event was a unique case. The organisation wanted to 
            make a dramatic entrance. Because they were coming from an already 
            strong brand (BP Oman) which had to be reinvented as Omanoil in the 
            minds of the audiences, the event had to be slightly, if I may say 
            so, shocking for some people. Some people, of course, absolutely 
            loved it while some of them maybe thought slightly less favourable 
            of it. But the idea was for the audience to register the new entity 
            firmly in their mind - and people will surely remember it,&quot; she 
            says. <br>
            <br>
            <b>The fastest growing segment </b><br>
            While the jury is still out on that one, the live concert circuit is 
            probably the fastest growing segment of the business. Experts 
            estimate that it has grown more than 200 per cent between 1999 and 
            2004. The last three years have witnessed a spate of international 
            artists coming to Oman to perform in live events. As Gazelle's 
            Sampat puts it, &quot;The country is starved of entertainment. Whatever 
            you do will work.&quot; Endorses Chowdhary: &quot;Entertainment was a very 
            neglected segment. Now that people have woken up to this segment, it 
            will see enormous growth.&quot; <br>
            <br>
            Others like Ideas' Gupta are less optimistic: &quot;I believe that 
            further growth is possible only in certain segments like sports 
            where we haven't seen too many events been organised. The rest of 
            the market is choc-a-bloc.&quot; Disagrees Momentum's Sarah: 
            &quot;Family-oriented events are obviously very important. Outside of the 
            working life, one of the nicest things about Oman is that the people 
            are very family-oriented, and such events go down particularly well. 
            Social and entertainment segment is quite key because of the 
            increased focus on tourism.&quot; <br>
            <br>
            Intevents' Jeremy Alston, director (sports events), differs. 
            &quot;Sporting events is an area which is fast catching up in this part 
            of the world. Dubai has some very good sports events. Some of the 
            events that we are planning are designed to compliment and take 
            advantage of the numbers of people who are interested in this 
            specific area and come to the Middle east for other sporting 
            events,&quot; he says. &quot;For instance, we're fortunate that the Oman 
            International Rally is happening shortly after the Bahrain Grand 
            prix, and we suspect that we'll certainly benefit from the surge in 
            interest in this sport,&quot; he adds.<br>
            <br>
            Counters Adinc's Raman: &quot;General entertainment is definitely 
            something that most people have been doing so far successfully, and 
            that's one area that I see as the fastest growing.&quot; That's exactly 
            why the company ventured into this niche with their World Fashion 
            show. &quot;A lot of our clients wanted us to do a premiere social event 
            because they were very satisfied with our premiere corporate event - 
            Oman Awards for Excellence. That's how the idea was born. We thought 
            fashion would be a category that might appeal across segments, and 
            it hadn't been done in Oman - at least not on such a scale.&quot; <br>
            <br>
            &quot;We were clear that we didn't want to make it a mass commercial 
            event and wanted to keep its exclusivity. So, when we did the plans 
            and costings for the show, we decided to approach certain key 
            clients, who thought that it was a great idea, and that's how we 
            took it forward. The idea of having segments in the fashion show was 
            for cost-effectiveness of the whole activity so that as more clients 
            could be factored in. That worked extremely well as the cost would 
            have been too much for one single client to bear,&quot; he adds. <br>
            <br>
            With Indian film celebrities and music artistes being close to the 
            heart of Indians in Oman, which comprise a majority of expatriate 
            population in the country, there is a huge potential for the event 
            management industry to organise live shows. With a large 
            concentration of South Indian population, Oman has been a favourite 
            stopover for South Indian film artists who have always performed to 
            a full house. <br>
            <br>
            The last few months have seen a plethora of such shows. These shows 
            have evoked tremendous response not only from the Indians, but also 
            from Pakistanis, Sri Lankans and Omani citizens. &quot;While we have had 
            Indian shows so far, we will soon be organising Arabic, English and 
            even Bangla shows,&quot; maintains Gazelle's Sampat. <br>
            <br>
            <b>Sponsorship or tickets?</b><br>
            Intevents' CxO summit, held in December last, featured opening and 
            closing keynote speeches from the Rt. Hon. Michel Portillo MP, 
            former Secretary of Defence, UK. The company managed to keep the 
            price of tickets within the common corporate citizen's reach because 
            &quot;Portillo didn't charge us a bomb as he was known to us from 
            earlier,&quot; reveals Intevents' Crawley. <br>
            <br>
            Not everyone, though, can expect to get lucky all the time. &quot;Keynote 
            speakers or stars from India, the US or Europe don't come cheap. In 
            cases where inviting a 'star' attraction will make the ticket prices 
            shoot up, we try to subsidise this price with sponsorship income,&quot; 
            explains Intevents' Alston. &quot;Our revenue model is based on delegate 
            / spectator income which is complimented by sponsorship income,&quot; 
            discloses Intevents' Hodge. &quot;And it works,&quot; he adds. <br>
            <br>
            Agrees NPA's Suvarna: &quot;Getting a star attraction like A.R. Rahman, 
            the Indian music prodigy, can be a $100,000 affair. You will need 30 
            sponsors if you were to get such a show sponsored, and that would 
            not give the requisite mileage to any of them as all of them would 
            be shouting for the audience's attention. The only way out is to 
            combine sponsorship with ticket-based sales.&quot; However, &quot;there is a 
            fine line between sponsorship and organising events independently,&quot; 
            maintains Intevents' Crawley. &quot;If you have too many sponsors on 
            board, you run the risk of over-commercialising the event, which 
            can, at times, bias the proceedings and defeat the very purpose of 
            an event,&quot; he claims.<br>
            <br>
            Not everyone subscribes to that theory, though. &quot;Unfortunately, the 
            population size of Oman isn't big enough. So, organisers have to 
            either keep the prices much higher or seek sponsorships,&quot; laments 
            Gazelle's Sampat. &quot;The complimentary pass culture in Oman has yet to 
            be overcome. Sponsorship by corporates helps ensure that the 
            expenses of organising an event are met,&quot; claims Gazelle's Chowdhary.
            <br>
            <br>
            Counters Adinc's Raman: &quot;People are definitely going to seek free 
            passes if they are available and thus avoid paying up for the event. 
            The ball is actually in the organisers' court. The lack of 
            availability of free passes will determine better success ratio.&quot; 
            Adds NPA's Suvarna: &quot;Organisers should minimise doling out free 
            passes if they are to succeed in the long run. Putting your foot 
            down now may lead to short-term suffering, but ultimately, they will 
            benefit from it.&quot; <br>
            <br>
            Explains Intevents' Crawley: &quot;In the past, there have been some poor 
            events - and people don't pay to go to poor events. You have to put 
            together an interesting event, and if it is a fair price, they will 
            pay you the money.&quot; Pricing, indeed, is a crucial issue. &quot;It all 
            depends on how much people can spend. If an event is reasonably 
            priced, it will click.<br>
            <br>
            It is just a matter of time that people overcome the lure of 
            complimentary passes and start paying up for quality shows,&quot; avers 
            Gazelle's Sampat. &quot;No event should be undertaken without the 
            assurance of basic expenses being met, whether it be the sponsorship 
            model or the ticket-based one,&quot; advocates Adinc's Raman. &quot;Most 
            people in Oman follow the sponsorship model where they don't rely on 
            entry tickets. Problem is that it limits the size of the event you 
            are able to do,&quot; he adds.<br>
            <br>
            &quot;If your product itself is correct, you can definitely bank on 
            ticket sales for your show. You have to do your planning very 
            accurately - you just cannot do a general plan as in that 'we will 
            get a big name' and then expect everything to just fall in place by 
            itself. Like any good communication plan, you should have a defined 
            strategy,&quot; adds Raman.<br>
