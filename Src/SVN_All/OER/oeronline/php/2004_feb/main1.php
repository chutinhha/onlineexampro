            Hitting the jackpot!</font></font><p align="justify"><b>
            <font face="Verdana" size="1">As the cash prizes offered by some 
            banks in Oman reach a new peak, Nayl D'Souza wonders whether local 
            deposit account holders are getting value for their money </font>
            </b></p>
            <p align="justify"><font face="Verdana" size="1">
            <img border="0" src="im1.jpg" align="left" width="191" height="211">Aminul 
            Hoque continues to scoot around Muscat on a motorbike delivering 
            newspapers despite scooping what has been described as the largest 
            cash prize in Oman's banking history. Last month, Oman International 
            Bank (OIB) announced he was the RO150,000 jackpot winner of the 
            bank's Mandoos savings scheme. Hoque says he has plans to start his 
            own business one day in the future. In the meantime, the unassuming 
            Bangladeshi maintains that he will continue working dutifully at 
            United Media Services, a company he joined as a newsboy in 1994. 
            It's a wonderfully endearing news story and one that is being milked 
            by the bank to dismiss a widespread belief among expatriates that 
            the chances of winning a top prize are confined to Omani nationals.
            <br>
            <br>
            It is not altogether surprising that National Bank of Oman (NBO) is 
            equally adamant that winners of its Savings Excitement scheme are 
            selected fairly. Last year, the bank named 338 lucky winners. Of 
            these, 85% were Omani, 15% expatriates. Leaving aside the scale of 
            payouts for simplicity, these results - more or less - tally with 
            the bank's customer base, according to the bank. <br>
            <br>
            Understandably, there are many other commonly held views about such 
            schemes that banks are happy to dismiss as myths. Shortly after his 
            triumph, Hoque announced that he had accumulated savings of RO6,000 
            in his bank account. This has raised the spectre of uncertainty that 
            those with a balance of RO1,000 deposited in their accounts (the 
            bare minimum needed to enter its grand prize draw) are overlooked. 
            This is, of course, untrue. Basically, the structure of most deposit 
            prize schemes in Oman is that for a pre-determined period, for every 
            'x' amount you deposit (usually RO100 for weekly raffles and RO1,000 
            or more for grander draws) you gain a virtual lottery ticket. <br>
            <br>
            &quot;The bank's computer that randomly selected Aminul Hoque did not 
            know who he was. It simply knew that he was eligible to win,&quot; says 
            Khalfan Al Tale'y, deputy general manager and head of retail 
            banking, OIB. The more you save, however, the more chance there is 
            of winning. <br>
            <br>
            So we have ascertained that the country's banking schemes seem fair 
            regardless of age, gender, nationality, or how much above the 
            minimum deposit you save. In fact, even the misconception that you 
            will only win once is false. Both OIB and NBO confirm that good 
            fortune has smiled on the same person twice, and for one jammy 
            customer it has struck thrice within a year (albeit none involving a 
            jackpot payout). <br>
            <br>
            So, deposit prize schemes are fair after all - or are they? <br>
            <br>
            <b>Called to account </b><br>
            The accounts being advocated by Oman's banks are internationally 
            known as lottery-linked deposit accounts. They are especially 
            attractive to low-income people. While the smarter investor eyes 
            trust funds, the equity market, gold and real estate among other 
            high-value yield instruments for value, customers who enter the 
            banking tombola are prepared to forgo interest or accept a risible 
            rate of return. Aside from this, the money's value is eroded by 
            inflation, and there are no guarantees of winning a prize. For most 
            blue-collar workers, this is not the wisest move to make to plump up 
            their nest eggs. For them, the seduction of lotteries proves too 
            overwhelming. <br>
            <br>
            On the flip side, the banks claim that their intentions are honest 
            and the schemes are not a glaring cash-in. The banks apparently wish 
            to bestow upon customers an incentive to earn financial stability 
            with a variety of savings options. They also convincingly argue that 
            unlike other raffles, the prizes are bigger and you don't lose your 
            money if you don't win.<br>
            <br>
            Said Al Badai, assistant general manager for branches of BankMuscat 
            (SAOG), says of the bank's flagship retail product: &quot;We consider the 
            Al Mazyona prize draw a great opportunity for the bank to give back 
            to the community and spread happiness amongst people, even as we 
            attempt to inculcate traditional values associated with savings.&quot;
            <br>
            <br>
            &quot;Getting into the habit of saving money is more important than how 
            much you're saving. Starting with small amounts can contribute 
            favourably towards establishing a pattern,&quot; adds another banker in 
            favour of such schemes. <br>
            <br>
            Keeping your money safe inside a lottery-linked account may not be a 
            hardship in Oman at this time. There is deflation in the local 
            economy (The Muscat Consumer Price Index declined to 96.4 points in 
            August 2003) and interest rates remain low. <br>
            <br>
            &quot;Herein lies the essence of risk and reward. Unlike bets in a casino 
            or any other gamble, capital, other than through the ravages of 
            inflation, is preserved. Interest is foregone for the chance to win 
            prizes. Yet, worryingly, there is no shortage of 'investors' who are 
            prepared to accept poor odds,&quot; observes Warren Edwardes, Chief 
            Executive of London-based Delphi Risk Management Limited. <br>
            <br>
            Ali Hassan Moosa, assistant general manager of operations, Alliance 
            Housing Bank (AHB) seems similarly unconvinced that consumers are 
            accepting a fair bet, saying, &quot;It is already noted that the vast 
            majority of participants get a bad deal by winning nothing. People 
            may overestimate their chances of winning because of their 
            excitement in anticipation of a win. <br>
            <br>
            &quot;Locally, a large number of people go for such accounts on religious 
            assumptions that interest is prohibited, but gifts are not.&quot; <br>
            <br>
            <b>Against the odds </b><br>
            A savings account is good to have and it certainly appeals to people 
            to save money in a bank when cash prizes are offered to them. But 
            shouldn't you know the odds of winning? Banks in Oman do not share 
            these despite the fact that elsewhere in the world this is a basic 
            requirement. For example, the odds of winning the top prize in the 
            UK's National Lottery are one in 13 million. <br>
            <br>
            &quot;Lotteries normally offer a small number of large prizes and a large 
            number of small prizes to keep the interest of participants. The 
            structure and characteristics of the pay scheme must be transparent 
            to participants so that they can estimate the odds and be able to 
            judge which scheme to enter into,&quot; remarks Moosa. <br>
            <br>
            Mehra Saif Al Hinai, 27, an administrative assistant at OIB, has 
            plans of opening up a savings account soon. She is prohibited from 
            choosing a prize scheme at the bank where she works to protect 
            against fraud. She says, &quot;Knowing what the chances are of winning 
            would be a great help. I cannot see a reason why we should not be 
            told.&quot;<br>
            <br>
            Despite such bewilderment, one banker in Oman declined to give odds 
            citing that to do so would &quot;weaken the bank's competitive edge.&quot; 
            Another said it would only empower customers with this information 
            if mandated to do so by the Central Bank of Oman (CBO). <br>
            <br>
            <b>Playing the numbers game </b><br>
            Earlier we had also discounted that the schemes had any bias towards 
            age or gender. Dig
            <img border="0" src="im5.jpg" align="right" width="175" height="173">deeper 
            and you'll find that women and children who deposit savings into 
            some banks get twice as many 'virtual lottery tickets' than adult 
            males for particular draws. It's a marketing ploy and an effective 
            one at that. The same products that disqualify men aged over 18 
            years from opening such accounts, actually allow them to double up 
            their participation by opening up an account for their child. Kid's 
            accounts are under the control of a parent or guardian until a child 
            is usually aged 12. There is no contention that banks are doing 
            this. It is, however, questionable whether the best way to foster 
            and promote saving amongst the young is for their parents to set up 
            such an account for reason of enabling them to get more throws of 
            the dice in the bank's lottery. <br>
            <br>
            &quot;We do not encourage children to participate in the draw but we do 
            inculcate their habit of saving. At the same time as encouraging 
            them to save, the children gain the opportunity to win prizes and 
            for that reason they have been given the privilege of receiving 
            double the opportunities to win,&quot; says Al Tale'y of OIB, a bank that 
            launched its popular Masroor savings account aimed at Oman's school 
            children in 1988. <br>
            <br>
            It is possible, however, that deposit prize accounts in Oman geared 
            towards kids could one day produce a scandal, according to an 
            industry spokesman. For example, children named as winners by a bank 
            may choose to sue their parents for misusing their winnings. As 
            mentioned, kids are totally reliant on their parents to operate the 
            bank account faithfully below the age of 12. Youngsters aged between 
            12 to 18 years share custody of their account and it is only at 18 
            when they usually gain full control of such accounts.<br>
            <br>
            <b>Treasured banking </b><br>
            Getting your children hooked to the idea of saving and managing 
            money bodes well for their future. Banks such as HSBC (Middle East 
            Ltd) with its HeadStart account steers clear of giving cash prizes 
            to, instead, offering incentives such as an attractive interest rate 
            and an easy way for children who want to save to learn the 
            importance of money management.<br>
            <br>
            &quot;I would gladly open a children's saving account in a local bank 
            which offered high value and secure investment products which would 
            be of benefit to him in the future but there isn't any choice. 
            Instead, my child's savings account earns money in a foreign bank,&quot; 
            concedes an Omani banker from one of the bank's peddling prize 
            schemes. <br>
            <br>
            <b>Branching out </b><br>
            The banks in Oman market inherently different savings accounts. &quot;A 
            scheme that offers a large prize per branch has different 
            characteristics than one that offers it for the entire country or to 
            participants at international branches,&quot; explains one banker. For 
            example, rather than give a huge cash prize, schemes like Oman Arab 
            Bank's Hassad Family Fortunes have a different focus. Not only do 
            they give special prizes for women and children but the bank also 
            distributes 16 branch prizes for a select branch every month. This 
            is done to ensure that winning accounts are more equally scattered 
            across the Sultanate rather than fall in a cluster around a 
            particular branch. <br>
            <br>
            <b>Banks battle for custom </b><br>
            To win yet more market share, some banks in Oman have opted to up 
            the ante and offer larger payouts as well as spend more on marketing 
            their draws. Prize schemes are dependent on heavy advertising. In a 
            fairly homogeneous market where most savings schemes offer prizes, 
            only those banks that convince the general public that their dreams 
            can really come true are the ultimate winners. <br>
            <br>
            Smaller banks are finding it difficult to match the larger banks in 
            terms of frequency of draws, richness of the prizes, and ad cost. It 
            is debatable how long they can sustain this policy without taking 
            risks that become too averse. Arguably, the local market is already 
            close to that stage. Analysts, however, forecast that the banking 
            market is unlikely to see dramatic increases in payouts and 
            administration of such schemes. They say other issues like foreign 
            competition, credit cards and e-banking take priority. <br>
            <br>
            In the future, perhaps we shall see banks in Oman choose some 
            interesting prizes other than cash to differentiate themselves from 
            one another. They may deliver investment options for children or 
            bonds and pensions for adult customers. Who knows? Banks may even 
            decide to give high interest on savings. It is not beyond the realms 
            of possibility. The sands of time would have gone full circle if 
            they do. Before the merger of Majan International Bank (MIB) with 
            Bank Dhofar in March 2003, MIB's policy was to create a niche for 
            itself and focus on professionals such as doctors, nurses, teachers 
            and engineers who had no hang-ups about the issue of receiving a 
            decent rate of interest per annum (6.75% at its peak). On the other 
            hand, there probably will be no change. <br>
            <br>
            &quot;I foresee that our bank will continue with deposit prize schemes 
            until the market conditions - namely, customer demand and the 
            activities of competing banks - determine that they should stop,&quot; 
            says Al Tale'y of OIB. <br>
            <br>
            Clearly, another factor that could also impact the market is more 
            regulation by the CBO. It could be argued that where the market 
            economy is not producing the desired results, the regulators have a 
            duty to intervene. <br>
            <br>
            <b>Risky business </b><br>
            For banks, prize schemes are a good bet because they are a cheap way 
            of raising funds. The bank wins if the overall cost of operating a 
            scheme with prize money is less than it would have been if it 
            offered interest. <br>
            <br>
            In contrast, the prize money is announced in advance regardless of 
            qualifying accounts. Therefore, the risk to the bank is that all the 
            prizes must be awarded by the end of the period regardless of monies 
            collected. Moreover, the competition between banks lead to periodic 
            adjustments of such schemes. <br>
            <br>
            &quot;As far as deposit volatility is concerned interest bearing deposit 
            accounts are normally 'sticky' (deposited for longer periods and 
            thus help reduce liquidity risk). Prize scheme money behaviour is 
            extremely sensitive to the prizes that are on offer. If a bank that 
            offers prizes stops while others continue, it is very likely that 
            substantial sums of money will be withdrawn from that bank. It 
            follows that once a bank enters into offering prizes, it is 
            extremely difficult to pull out,&quot; says Moosa of AHB. <br>
            <br>
            <b>A winning streak? </b><br>
            It would be remiss not to give you the latest news from Aminul Hoque, 
            the jackpot winner mentioned earlier. He plans to keep his money 
            parked in his OIB bank account. This will provide him with hundreds 
            of additional lottery entries into the bank's lottery scheme to the 
            possible envy of every luckless savings account holder in the 
            country. </font></p>
