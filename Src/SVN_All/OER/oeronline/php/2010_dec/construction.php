					</font><font face="Verdana" size="2"><b>Cost consultancy is 
                    an integral part of the construction industry and clients 
                    should take advantage of these services, says Millan De 
                    Silva, CEO, Milcris Pvt Ltd<br>
                    </b></font><font face="Verdana" size="1"><br>
                    <img border="0" src="images/construct.jpg" align="left" hspace="12" vspace="6" width="350" height="235">Even as the Sultanate witnesses new infrastructure 
                    developments like roads, railways, airports and enhanced 
                    tourism projects, it is becoming prominently visible that 
                    the immense benefits of having project cost consultants are 
                    not yet being adequately harnessed,” says Millan De Silva, 
                    CEO, Milcris, a Chartered Quantity Surveying, Cost 
                    Consultancy and Project Management company.<br>
                    <br>
                    Cost consultancy, which comes under the purview of Quantity 
                    Surveyors (QS), covers a wide range of contract and 
                    commercial management services, which immensely assist the 
                    client to plan out development and execute projects in 
                    keeping with budgets. The designation ‘Quantity Surveyors’ 
                    is somewhat misleading when compared with the scope of 
                    services that are being performed by these professionals who 
                    are also referred to as commercial managers of a project. 
                    Their activities range from feasibility report stage to 
                    completion of the project and agreeing of the final account 
                    and address procurement strategy, tender management, 
                    contract documentation and interpretations, change and claim 
                    management, dispute resolution amongst a host of other 
                    important roles.<br>
                    <br>
                    “The most effective contribution to the client will be when 
                    the cost consultant enjoys direct access and dialogue with 
                    the client, and then the impact of their advice will be 
                    fully understood by the client. Some government 
                    organisations such as Royal Estates, Ministry of Defence and 
                    the Muscat Municipality have understood the importance of 
                    this aspect. Nevertheless, a very large volume of work is 
                    handled by clients who act contrary to this, thus depriving 
                    themselves of the desired benefit,” adds De Silva.<br>
                    <br>
                    <b>Cost implications</b><br>
                    “From the point of the Master Plan, the QS provide advice to 
                    the client and the design team on cost implications of the 
                    design and its changes and also the contractual implication 
                    that may arise with any changes in the project. The major 
                    risk to any development project is the possibility of a 
                    budget over-run and hence the importance of cost management 
                    as against cost reporting,” says De Silva.<br>
                    <br>
                    Milcris are the cost consultants to the prestigious 
                    multi-billion dollar landmark development of the Muscat and 
                    Salalah International Airport projects in the Sultanate, 
                    which incidentally, is the largest investment currently in 
                    the Sultanate. “Today the world is rapidly changing to 
                    specialisation just like one seeks a specialist doctor 
                    instead of a general practitioner and hopefully going away 
                    from the age old approach where the client initially 
                    appoints the designer for the project who in turn appoints 
                    the cost consultant as a sub consultant,” &#8232;says De Silva.<br>
                    <br>
                    The QS profession is enriched by the strict codes of 
                    conduct, best practices, standards and ethics of the Royal 
                    Institution of Chartered Surveyors, UK which is an impartial 
                    professional body. “Our staffs comprise of 11 chartered 
                    members amongst 60 QS and we are committed to provide best 
                    services to our clients in keeping with the RICS best 
                    practices and ISO 9001.2008 quality procedures,” concludes 
                    De Silva.</font></p>
