				<font face="Verdana" size="1"><b><br>
				</b></font><b>
				<font face="Verdana" size="2">Notwithstanding the dent created by the recall of thousands of 
                cars by leading automakers, the demand for outstanding cars has 
                not diminished<br>
                </font></b>
				<font face="Verdana" size="1">
                <br>
                <br>
                </font><b>
				<font face="Verdana" size="2" color="#C00000">
                <img border="0" src="images/cover57.jpg" align="right" hspace="12" width="200" height="124">Audi S4</font></b><font face="Verdana" size="1"><br>
                Enter the 2010 Audi S4. It’s actually sporty, a real hoot to 
                drive. Though less powerful than its predecessor, this 
                slick-looking 4-door is faster and produces a relentless wave of 
                torque that completely contradicts the manufacturer’s ratings. 
                Plus, it costs less and is 20 percent more fuel efficient. The 
                all-new S4 lost a couple of cylinders but packs a silky, 333-hp 
                supercharged V-6 (it makes just seven fewer horses than the old 
                V-8 did) and might be the most controversial car on this year’s 
                list. Practicality is also on the S4’s side. Exceptional 
                ergonomics, good comfort (especially in the rear seat), and 
                class-leading styling round out its winning package.<br>
                <br>
&nbsp;</font><hr color="#CC3300" size="1"> 
				<p align="justify" style="margin: 3px 10px">
				<font face="Verdana" size="1"><br>
                </font><b><font face="Verdana" size="2" color="#C00000">
                <img border="0" src="images/cover58.jpg" align="left" hspace="12" width="200" height="122">Ford Fusion Hybrid<br>
                </font></b>
				<font face="Verdana" size="1">It’s an unlikely claim, but the Ford Fusion hybrid is in fact 
                the most advanced car on this list. Through the body of this 
                unpretentious family sedan runs the sturdiest bridge between the 
                tech of the 20th century and that of the 21st. The Fusion’s 
                hybrid powertrain is so refined and compact that it’s almost not 
                there, but the driver-selectable, power-tracking gauge display 
                gives the game away: It is Donkey Kong addictive, challenging 
                its driver to run on electric power all the way up to a possible 
                47 mph. This is a boon to both car guys and the unaffiliated – 
                you can drive it for fun (a hybrid first) or for mileage (a 
                class-leading 41 city/36 highway), which is also fun.<br>
                <br>
&nbsp;</font><hr color="#CC3300" size="1"> 
				<p align="justify" style="margin: 3px 10px">
				<b><font face="Verdana" size="2" color="#C00000"><br>
                <img border="0" src="images/cover59.jpg" align="right" hspace="12" width="210" height="128">Cadillac CTS / CTS-V</font></b><font face="Verdana" size="1"><br>
                Cadillac CTS offers the CTS-V, a 556-hp supercharged beast that 
                brawls eagerly with the very best sports sedans from Germany. 
                Many consider the CTS-V to be the best American car ever made. 
                Put that assertion aside, and the CTS-V is still a bargain 
                alternative to the supersedans from Germany. Its interior is 
                gorgeous, with a truly expensive feel. Outside, the CTS combines 
                uniform shapes and sharp angles that borrow from nobody. But the 
                way the CTS drives is what we keep falling for year after year – 
                its moves are as crisp as its exterior lines. The CTS manages to 
                blend refinement, driving dynamics, comfort, and performance in 
                a refreshing, even exhilarating, way that satisfies enthusiast 
                drivers and luxury seekers simultaneously.<br>
                <br>
&nbsp;</font><hr color="#CC3300" size="1"> 
				<p align="justify" style="margin: 3px 10px">
				<b><font face="Verdana" size="2" color="#C00000"><br>
                <img border="0" src="images/cover60.jpg" align="left" hspace="12" width="200" height="147">Honda Fit</font></b><font face="Verdana" size="1"><br>
                It’s the car that suits everyone’s needs – big fun, and as cheap 
                to own and operate as a hamster. Despite the Fit’s microbial 
                footprint, it takes truly Sasquatchian dimensions to be 
                discomforted inside, and all that space is surrounded by quality 
                materials and thoughtful design unmatched in many vehicles 
                costing much more. While the 1.5-litre’s 117 horsepower won’t 
                much squish the seatback foam, keeping the four-pot revving can 
                make for an engaging game with either of its two available 
                transmissions. With short shifts and direct, mechanical 
                movement, the base five-speed manual should be studied by the 
                world’s finer sports-car makers. Pairing the automatic with the 
                Sport model nets an obedient paddle-shifted slushbox that does 
                the cheapest F1 impression on the market.<br>
                <br>
                <br>
                &nbsp;</font><hr color="#CC3300" size="1"> 
				<p align="justify" style="margin: 3px 10px">
				<b><font face="Verdana" size="2" color="#C00000"><br>
                <img border="0" src="images/cover61.jpg" align="right" hspace="12" width="200" height="122">Mazda 3 / Mazdaspeed 3</font></b><font face="Verdana" size="1"><br>
                The latest Mazda 3 arrives with more than just a shiny new 
                wrapper. It’s more refined, too. In both 148-hp i and 167-hp s 
                versions, the basic Mazda 3 feels more like a genuine car than 
                most of the econoboxes it competes with on price – a bare-bones 
                i starts at $16,045, and the s begins at $19,790 (US prices). 
                Material quality, ride quality, and chassis control are all a 
                class above. If there are beancounters at Mazda, they don’t get 
                anywhere near the suspension. Getting behind the wheel of the 
                Mazda 3 is a comforting reminder that there is a fun-to-drive 
                car for every economic stratum.</font><p align="justify" style="margin: 3px 10px">
				&nbsp;<p align="justify" style="margin: 3px 10px">
				&nbsp;<hr color="#CC3300" size="1"> 
				<p align="justify" style="margin: 3px 10px">
				<b><font face="Verdana" size="2" color="#C00000"><br>
                <img border="0" src="images/cover62.jpg" align="left" hspace="12" width="200" height="122">Porsche Boxster/Cayman</font></b><font face="Verdana" size="1"><br>
                There are a lot of words beginning with the letter “P” that can 
                describe the mechanically identical Boxster and Cayman – poised 
                and profitable leap to mind – but the most appropriate one is 
                perfect, when the Boxster offers a driving experience so sublime 
                and so tactile that it’s really only rivaled by the Cayman’s. 
                Every spin of the steering wheel, every push of a pedal, every 
                fat blat from the flat-six engines, they’re all calibrated to 
                deliver one thing: unparalleled automotive pleasure. In fact, 
                the pair proves even more rewarding than the iconic – and much 
                pricier – 911. They’re livable and luxurious, with good cargo 
                room from their two trunks as well as supportive seats and 
                leather everywhere (including on the coat hooks and vent slats).<br>
&nbsp;</font><hr color="#CC3300" size="1"> 
				<p align="justify" style="margin: 3px 10px">
				<b><font face="Verdana" size="2" color="#C00000"><br>
                <img border="0" src="images/cover63.jpg" align="right" hspace="12" width="200" height="122">Volkswagen GTI</font></b><font face="Verdana" size="1"><br>
                The latest GTI is the sixth iteration since VW invented the 
                pocket rocket &#8232;(or “hot hatch” in Euro-speak) back in 1976. And 
                while the GTI has grown larger and more powerful, it keeps its 
                original spirit. <br>
                <br>
                It’s relatively inexpensive and supremely practical yet is an 
                immensely entertaining vehicle that’s as happy meandering around 
                mall parking lots as it is being flogged along a great back 
                road. The 200-hp, turbocharged inline four-cylinder is smooth 
                and responsive, although it doesn’t imbue the GTI with the kind 
                of startling acceleration that the brawnier Mazdaspeed 3 
                possesses.<br>
                <br>
&nbsp;</font><hr color="#CC3300" size="1"> 
				<p align="justify" style="margin: 3px 10px">
				<b><font face="Verdana" size="2" color="#C00000"><br>
                <img border="0" src="images/cover64.jpg" align="left" hspace="12" width="200" height="122">BMW 3-series / M3</font></b><font face="Verdana" size="1"><br>
                It has a cohesive fluidity that’s unmatched by any of its 
                competitors. But then, the 3 feels similarly sublime on any 
                other stretch of road, too. From the 230-hp 328i that can 
                achieve 30-plus mpg on the highway to the even thriftier and 
                uncharacteristically responsive diesel to the unforgettable bawl 
                of the M3’s screaming 414-hp V-8 at 8400 rpm that gets the 
                0-to-60-mph job done in just over four seconds, the 3-series 
                range is extremely broad. Body styles consist of coupe, sedan – 
                including the resurrection of a four-door M3 – wagon, and 
                folding-hardtop convertible. The 3-series has earned a long list 
                of comparison-test victories because of the instant confidence 
                 
                that it imparts to the driver.<br>
                <br>
&nbsp;</font><hr color="#CC3300" size="1"> 
				<p align="justify" style="margin: 3px 10px">
				<font face="Verdana" size="1"><br>
                </font><b><font face="Verdana" size="2" color="#C00000">
                <img border="0" src="images/cover65.jpg" align="right" hspace="12" width="200" height="141">Lexus LS 460L</font></b><font face="Verdana" size="1"><br>
                If you are looking for a luxurious seating and comfortable 
                driving experience, the Lexus LS 460L is one of the best cars to 
                choose from. The 4.6 litre V8 engine of this Lexus car generates 
                380 hp power and produces a torque of 360 lb-feet. The 
                specifications of power and torque for the all-wheel drive model 
                are 357 hp and 344 lb-feet respectively. The Lexus LS 460L takes 
                no more than 6 seconds to reach the speed of 60 mph. The looks 
                are enhanced by a thick D-pillar and curve given to the 
                roofline. One of the notable features of this car is the air 
                purifier system. Electric shades on the back portion and rear 
                doors block the sun light. The air suspension system allows for 
                a smooth ride. To make the ingress and egress easy, the electric 
                seat slightly moves back. Steering wheel is telescoping and 
                slightly tilted.<br>
                <br>
&nbsp;</font><hr color="#CC3300" size="1"> 
				<p align="justify" style="margin: 3px 10px">
				<font face="Verdana" size="1"><br>
                </font><b><font face="Verdana" size="2" color="#C00000">
                <img border="0" src="images/cover66.jpg" align="left" hspace="12" width="200" height="133">Mercedes-Benz E350</font></b><font face="Verdana" size="1"><br>
                Mercedes mid-level E-Class is not only their best-selling model 
                in the US but also one of the favourite luxury cars. The all-new 
                E-Class sedan is available in six-cylinder (E350) and 
                eight-cylinder (E550) form, along with the diesel-powered E350 
                BlueTEC. New features include night vision high beams that 
                automatically adjust to avoid blinding oncoming traffic. The 
                2010 E is also home to a host of new gadgets, including 
                attention assist, which senses if the driver is getting drowsy 
                and sounds an alarm. Options include a night vision camera that 
                highlights pedestrians and a parking assist system that tells 
                you when to turn the steering wheel.<br>
