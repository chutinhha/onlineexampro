				<b><font face="Verdana" size="2">The Public Authority for Radio and Television<br>
                <font color="#C00000">Transforming  the media</font><br>
                </font></b>
				<font face="Verdana" size="1"><br>
                The establishment of The Public Authority for Radio and 
                Television by Royal Decree No 108/2010 in October is a major 
                landmark for the media sector in Oman. The authority will have 
                its juristic personality, public authorities, financial and 
                administrative independence.<br>
                <br>
                The Authority, according to the royal decree, shall have the 
                authority to manage its funds and take actions towards them. It 
                shall be under the supervision of the Information Minister and 
                shall carry out its activities as per the commercial rules.<br>
                <br>
                The Authority shall be based in Muscat Governorate and it may 
                establish branches inside the Sultanate and foreign offices as 
                well, as per the dictates of public interest and according to a 
                decision by its board of directors. The royal decree will give a 
                boost to the broadcasting sector, as it is expected to come out 
                with more programmes on education, science, sports, light 
                entertainment and general knowledge.<br>
                <br>
                The electronic and print media in the Sultanate have played a 
                pivotal role in reflecting the development and progress of the 
                Sultanate. During the year, the first privately owned FM station 
                in the Sultanate heralded a new era in broadcasting, bringing to 
                an end more than three decades of dominance by the state-owned 
                electronic media. Hala 102.7 FM, the first privately owned FM 
                station in the Sultanate, was pioneered by the OHI Group, a 
                leading Omani business house. Sabco Media launched its Al Wisal 
                Arabic FM radio channel, with the motto ‘talk less, play more 
                (music)’. </font><hr color="#000000" size="1"></td>
