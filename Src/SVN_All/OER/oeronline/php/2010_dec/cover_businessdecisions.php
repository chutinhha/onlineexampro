                Following closely on the heels of a royal directive to plant one 
                million date palms in 2009, His Majesty Sultan Qaboos bin Said, 
                issued a fresh order to grow 100,000 coconut trees in the 
                southern Dhofar governorate.<br>
                <br>
                The Royal Orders to plant the coconut trees come within the 
                framework of the Royal attention of His Majesty to the 
                agricultural sector in the Sultanate and care for it all over 
                the country, stated Sayyid Ali bin Hamoud Al Busaidi, minister 
