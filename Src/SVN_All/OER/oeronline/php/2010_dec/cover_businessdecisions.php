                <br>
                Oman’s banking regulatory authority, The Central Bank of Oman (CBO), 
                gave the nod for setting up Al Izz International Bank, a 
                commercial bank. Al Izz International Bank will be the seventh 
                locally incorporated bank after BankMuscat, the National Bank of 
                Oman, Bank Dhofar, Bank Sohar, Oman International Bank, Oman 
                Arab Bank and Ahlibank. The decision, to give the green signal 
                to new bank was taken at a meeting of the CBO Board of 
                Governors, which met under the chairmanship of Dr Ali Bin 
                Mohammad Bin Mousa, deputy chairman of the CBO Board of 
                Governors and chairman of the Tender Board.<br>
                <br>
                Al Izz International Bank plans to launch an initial public 
                offering (IPO) in the third-quarter of 2011. The new financial 
                institution will have a capital of RO100mn and expects to begin 
                operations after an IPO issue, which will raise 40 per cent of 
                the capital while the rest will come from the promoters of the 
                bank. The minimum capital required for a new bank, as per CBO 
                regulations, is RO100mn, while the minimum capital requirement 
                of new branches of foreign banks is RO20mn. Meanwhile, a second 
                proposal for establishing yet another bank, the Nizwa Bank is 
                awaiting the nod from CBO.<br>
