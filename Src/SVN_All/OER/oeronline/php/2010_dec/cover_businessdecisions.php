                Minister of Agriculture, also expressed his gratitude for the 
                Royal grant issued by His Majesty to plant the 100,000 coconut 
                trees. The Royal orders embody His Majesty’s wisdom and 
                farsighted vision to develop the agriculture sector and benefit 
                from the Sultanate’s climatic diversity. The aim is to reach a 
                balanced agricultural development that covers all regions of the 
                Sultanate and is a means of achieving food security for the 
                people of Oman. A landmark symposium on ‘Sustainable development 
                of agricultural sector’ was held in 2009 at the Royal Camp at 
                Saih Al Masarat in Ibri to discuss agricultural issues. During 
                this visit, His Majesty the Sultan issued Royal Directives to 
                plant one million date palm trees in view of the fact that these 
                trees had been the backbone of food security in Oman alongside 
                other plantations, animal wealth and fisheries, which are among 
                the great wealth resources for the country.</font><hr color="#000000" size="1"> 
