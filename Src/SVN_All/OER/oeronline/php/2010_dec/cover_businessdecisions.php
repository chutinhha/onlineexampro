				<b><font face="Verdana" size="2">Mega power plants<br>
                <font color="#C00000">Energising the nation</font></font></b><font face="Verdana" size="1"><br>
                <br>
                The demand for electricity in the Sultanate of Oman is expected 
                to increase at a rate of around eight per cent a year due to its 
                expanding economy in the next five to 10 years. The demand is 
                being fuelled by not only population growth but also by huge 
                investments in tourism, industrial and real estate projects. <br>
                <br>
                Projections made by Oman Power and Water Procurement Company (OPWP), 
                which oversees procurement of new power and related water 
                capacity, envisions a requirement of between 2,900 MW and 4,100 
                MW of additional power generation resources within the Main 
                Interconnected System (MIS) of Oman. An estimated half a million 
                customers are served by the MIS, in areas encompassing the 
                Governorates of Muscat and Buraimi, most of South Batinah, 
                Dakhiliyah, Sharqiyah, North Batinah and Dhahirah regions.<br>
                <br>
                The aggressive development of the power sector is being marked 
                by investments in truly world-scale greenfield power generation 
                projects. OPWP awarded mandates to a consortium led by France’s 
                GDF Suez Group for the development of a pair of Independent 
                Power Projects (IPPs)  at Barka and Sohar. Barka 3 and Sohar 2, 
                as the IPPs are called, with  a total investment of around 
                $1.7bn for both projects.<br>
                <br>
                In Salalah, the foundation stone for a RO385mn power and water 
                desalination plant in Al Shur plain in the wilayat of Mirbat, 
                was laid. The Salalah IWPP will consist of a gas-fired power 
                plant which will have a net capacity of 445 megawatts and a 
                seawater desalination plant which will employ reverse osmosis 
                technology to produce 15 million imperial gallons per day of 
                water.</font><hr color="#000000" size="1"> 
