				<b><font face="Verdana" size="2">Traffic Safety Symposium<br>
                <font color="#C00000">Reining in the scourge</font><br>
                <br>
                </font></b>
				<font face="Verdana" size="1">His Majesty Sultan Qaboos bin Said approved a 20-point 
                recommendation mooted by the Traffic Safety Symposium (TSS) held 
                under the theme ‘Yes to Limiting Traffic Accidents’ with an 
                order for immediately implementing them.<br>
                <br>
                Lt Gen Malik bin Sulaiman al Maamari, Inspector General of 
                Police and Customs, affirmed that all respective government 
                departments will implement these recommendations and soon there 
                will be a number of procedures to activate them. Among the new 
                initiatives, October 18 was agreed upon as annual Traffic Safety 
                Day. Additionally, the setting up of a national centre for road 
                traffic statistics and information were agreed upon.<br>
                <br>
                The 20 recommendations made by the Traffic Safety Symposium came 
                after five days of brainstorming sessions in which national and 
                international experts participated to find solutions to limit 
                traffic accidents in the Sultanate. The 20-point strategy is to 
                fight the scourge bedeviling the country in the form of alarming 
                number of road accidents, maiming and killing citizens. His 
                Majesty had last year termed road accidents as a tax on the 
                public.<br>
                <br>
                The National Committee for Road Safety, the apex body for 
                overseeing the 10-year plan from 2011 to 2020, has an onerous 
                task on its head in setting targets, prioritising focus areas 
                and orchestrating harmonious functioning of all departments and 
                organisations, both governmental and non-governmental.</font><hr color="#000000" size="1"> 
