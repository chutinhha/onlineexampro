				<b><font face="Verdana" size="2">Oman Trade &amp; Investment Forum, UK<br>
                <font color="#C00000">Highlighting opportunities </font></font>
                </b>
				<font face="Verdana" size="1"><font color="#C00000"><br>
                </font> <br>
                Over 300 delegates, including around 60 senior Omani government 
                and business executives, attended the Oman Trade &amp; Investment 
                Forum which was held at London’s prestigious Mansion House on 
                the occasion of the 40th National Day of Oman. The Omani 
                delegation was led by HE Maqbool bin Ali Sultan, Minister of 
                Commerce and Industry and Chairman of OCIPED, and included HE 
                Khalil Abdullah al Khonji, Chairman of Oman Chamber of Commerce 
                and Industry (OCCI), and Dr Salem ben Nasser al Ismaily, CEO, 
                OCIPED.<br>
                <br>
                UK has the largest number of investments in the Sultanate of 
                Oman and it was only imperative that more British private sector 
                investment in the country takes place to sustain the strategic 
                relationship between the two countries. The UK is a strong 
                trading partner for Oman with bilateral trade being worth over 
                £450m last year . In the first Quarter of 2010 exports had 
                increased by a further 40 per cent.<br>
                <br>
                HE Maqbool encouraged British companies to look at Oman as a 
                strategic location using it as a hub for trade with other Middle 
                Eastern countries, East Africa and further afield, benefiting 
                from Oman’s political and economic stability, its excellent 
                relations with neighbouring countries and its friendly and 
                flexible laws which welcome foreign investments. The forum 
                examined business opportunities for partnership in key areas 
                including project finance; financial and legal services; power, 
                water and renewable energy; Salalah and Sohar Free Trade Zones; 
                education and skills development; and tourism and hospitality. 
                The forum also provided a framework to discuss partnership 
                opportunities between Oman and the UK, building on the existing 
                strong bilateral relations and to establish new business ties to 
                raise the profile of  Oman in the UK.</font><hr color="#000000" size="1"> 
