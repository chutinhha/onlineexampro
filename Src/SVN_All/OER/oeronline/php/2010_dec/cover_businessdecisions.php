				<b><font face="Verdana" size="2">Masirah bridge <br>
                <font color="#C00000">A link to the mainstream</font><br>
                </font></b>
				<font face="Verdana" size="1"><br>
                A 35-km long bridge connecting Masirah island to the mainland 
                has been envisaged by the ministry of finance, under the royal 
                directive of His Majesty Sultan Qaboos bin Said. Once it is 
                ready in 2013, the Masirah bridge will boost trade and tourism 
                to the isle. <br>
                <br>
                The Masirah Island, located off the southeast coast of Oman in 
                the  Arabian Sea, has a base of Royal Oman Air Force, is 
                sparsely populated, and would have remained a recluse had it not 
                been for the government which undertook a drive to popularise 
                the tourist attractions of the island, namely the turtle nesting 
                areas as well as other flora and fauna. But the only way for 
                citizens and tourists to and from the island was an hour long 
                ride in the  ferry boat which used to be rendered useless during 
                natural calamities like cyclones.<br>
                <br>
                Masirah has a population of around 12,000, about 20 per cent of 
                them are expatriates employed by the air force and civil work 
                contractors, according to Manpower Ministry statistics.<br>
                <br>
                Despite having white sandy beaches, clear water for snorkelling 
                and being home to rare turtles, the number of tourists visiting 
                the island is about two to three thousand a year, out of a 
                million tourists Oman receives a year. All that will change once 
                the bridge is laid.</font><hr color="#000000" size="1"> 
