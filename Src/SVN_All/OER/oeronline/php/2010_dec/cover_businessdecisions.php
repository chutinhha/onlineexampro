				<font face="Verdana" size="2">Muscat and Salalah airports development<br>
                <font color="#C00000">Spurring economic growth</font></font></b><font face="Verdana" size="1"><br>
                <br>
                <img border="0" src="images/cover12.jpg" align="right" hspace="12" vspace="12" width="344" height="184">Contracts worth more than $2bn have been awarded this year by 
                the Oman government to upgrade the country’s two main airports – 
                Muscat and Salalah – in a bid to cater to the burgeoning air 
                traffic in the country. The upgradation works for the two 
                airports are besides the five new airports at Sohar, Ras Al Hadd, 
                Duqm, Adam and Shaleem.<br>
                <br>
                The much-awaited RO294mn-Salalah airport expansion project was 
                bagged by a consortium led by India’s engineering conglomerate 
                Larsen &amp; Toubro (L&amp;T). The local partner in the consortium is 
                Galfar Engineering and Contracting Company.<br>
                <br>
                The scope of work for Salalah airport include design and 
                building of a new terminal, a 4 km-long runway, air traffic 
                control tower, new fuel farm and fuel network, new rescue and 
                fire fighting station, new aircraft parking hangar, air side 
                works and other facilities. The airport will be able to handle 
                one million passengers per year after the expansion. This can be 
                scaled up to two million. The RO706mn contract for building the 
                Muscat airport passenger terminal building was bagged by a 
                consortium of US-based Bechtel, Bahwan Engineering Company and 
                Turkey’s INCA.<br>
                <br>
                The airport terminal, which will be completed within 38 months, 
                will be designed to handle 12 million passengers annually. The 
                airport is designed in such a way to scale up the terminal 
                passenger handling capacity to 24 million at a later stage.<br>
                <br>
                The Muscat International Airport, which is designed to 
                accommodate large Airbus 380 planes, will have 29 aircraft 
                contact stands and 10 to 30 remote parking stands, depending on 
                phasing options. Once operational, the airport will also have 60 
                departure check-in counters for economy class and another 24 
                counters for passengers of other class, which is in addition to 
                several kiosks for easy check-in. The departure section will 
                also have 29 boarding gates for different destinations, and 
                another 15 boarding gates for remote aircraft stands.</font><hr color="#000000" size="1"> 
