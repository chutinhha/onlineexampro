					<b><font face="Verdana" size="4" color="#C00000"><br>
                    On a growth trajectory<br>
                    </font><font face="Verdana" color="#C00000" size="2">&nbsp;</font><font face="Verdana" size="4" color="#C00000"><br>
                    </font></b><font face="Verdana" size="2"><b>Qatar is 
                    emerging as an international player to be reckoned with, 
                    especially with regards to outward and inward investments</b></font></p>
                  <p align="justify" style="margin: 3px 10px"> 
					&nbsp;</p>
                  <p align="justify" style="margin: 3px 10px"> 
					<font face="Verdana" size="1">
                    <img border="0" src="images/closeup1.jpg" align="left" hspace="12" width="127" height="135">Qatar continues to grab international attention making bold 
                    acquisitions and becoming a global leader in per capita 
                    income. Qatar Holding acquired a 5 per cent stake at Banco 
                    Santander Brazil at the cost of $2.7bn, in October this 
                    year.<br>
                    <br>
                    There is something special about Qatar Holding, being a 
                    fully-owned affiliate of Qatar Petroleum, a major 
                    state-owned entity. Qatar Petroleum serves as a leader in 
                    the country’s economic development partly by turning Qatar 
                    into the world’s largest exporter of liquefied natural gas 
                    (LNG).<br>
                    <br>
                    At the same time, this very investment serves the interest 
                    of the Brazilian bank by virtue of connecting it to one of 
                    the fastest growing economies in the world. The IMF predicts 
                    nominal growth rate of 18.5 per cent for Qatari economy in 
                    2010 on the back of firm energy prices. <br>
                    <br>
                    The investment at Banco Santander Brazil is a rewarding 
                    development for Brazil by virtue of coming only months after 
                    the visit of Qatari Emir Shaikh Hamad bin Khalifa Al Thani 
                    to the country as part of a regional tour.<br>
                    <br>
                    <b>Harrods and Porsche<br>
                    </b>Also in 2010, Qatar captured the global attention 
                    through purchase of London’s department store of Harrods at 
                    $2.3bn from the Egyptian-born tycoon Mohammed Al Fayed. In 
                    2009, Qatar made another exceptional venture acquiring a 17 
                    per cent stake of the integrated Volkswagen and Porsche firm 
                    following merger of the two concerns. The deal included 
                    Porsche establishing a research and development and testing 
                    facility in Doha. Porsche and Qatar Holding agreed to 
                    explore other areas of co-operation in the fields of 
                    research and development, technical service and support in 
                    Qatar.<br>
                    <br>
                    <b>Attracting FDI</b><br>
                    Foreign direct investment (FDI) is not a one-way street. 
                    Recently published statistics put Qatar as the second 
                    largest recipient of FDI among the Gulf Cooperation Council 
                    (GCC) countries. This was put on display at the World 
                    Investment Report 2010 of the World Conference on Trade and 
                    Development (Unctad) which traces movements of FDI around 
                    the world.<br>
                    <br>
                    In particular, Qatar managed to more than double inward FDI 
                    from $4.1bn in 2008 to $8.8bn in 2009. The credit partly 
                    relates to sustained progress of the country’s energy 
                    sector, notably gas. Qatar is the largest exporter of LNG in 
                    the world, a position that it overtook from Indonesia 
                    several years ago. The country continues to expand its gas 
                    industry with annual LNG output amounting to 54 million 
                    tonnes, up from 38 million tonnes only three years ago. 
                    Still, the plan calls for reaching an output level of 77 
                    million tonnes a year by 2012.<br>
                    <br>
                    <b>
                    <img border="0" src="images/closeup.jpg" align="right" hspace="12" vspace="6" width="350" height="335"></b>Conversely, the Unctad report points to a general drop in 
                    inward investments in GCC in 2009, partially due to the 
                    global financial crisis. For instance, Saudi Arabia, the 
                    largest regional recipient of FDI, saw the value of inward 
                    investments dropping from $38.1bn in 2009 to $35.5bn in 
                    2008. Undoubtedly, FDI is a sort of international voice of 
                    confidence in different economies. More than 200 countries 
                    attempt to attract foreign investments to help them address 
                    economic challenges. FDI helps spurring growth in the local 
                    economy and serves as a means of providing employment 
                    opportunities. Qatar is proving that FDI is a two-way 
                    street, by attracting more foreign investments and 
                    concurrently investing abroad.<br>
                    <br>
                    <b>Firm budgetary spending</b><br>
                    Chances are that Qatar would be able to entice more FDI in 
                    the years to come on the back of stronger spending on 
                    infrastructure, as evidenced in the budget of fiscal year 
                    2010-11, which commenced at the start of April. Total 
                    governmental expenditures are put at around $32.4bn compared 
                    to $26bn in fiscal year 2009-10. Undoubtedly, this is a 
                    sizable growth in a single year.<br>
                    <br>
                    The authorities appropriated nearly $12bn or 37 per cent of 
                    total spending on development projects. Among others, the 
                    allocations cover infrastructure projects including the New 
                    Doha International Airport, New Doha Seaport plus road 
                    network, sewage systems, land reclamations and expansion of 
                    electricity and water capacities. On another positive note, 
                    the government allocated 7.2 per cent and 6.3 per cent of 
                    expenses for health and education, respectively. In turn, 
                    this kind of investment will strengthen Qatar’s position on 
                    the Human Development Index (HDI), which relies on three 
                    variables, namely life expectancy at birth, education and 
                    income on purchasing power parity basis (PPP).<br>
                    <br>
                    <b>Per capita income</b><br>
                    Against this backdrop, Qatar clinched the number 38 ranking, 
                    the second best in the GCC after the UAE, in 2010 Human 
                    Development Report (HDR), published by the United Nations 
                    Development Programme (UNDP).<br>
                    <br>
                    The same report put Qatar’s gross domestic product (GDP) per 
                    capita income on PPP basis at $79,426 per annum, ranked the 
                    second highest result in the world only after Liechtenstein. 
                    This years’ issue of HDI focuses on people, the real and 
                    sustained wealth of any nation. </font></p>
