<p><font face="Verdana" size="1">Financial globalisation has its benefits. 
It has its risks as well. The transformation of the banking industry in Oman and 
the Gulf is evident from the changes that have occurred in markets, institutions 
and products. While globalisation is opening up new vistas for banks in 
developing markets, it has entailed greater competition and, consequently, 
greater risks. <br>
<br>
<b>The Oman outlook </b><br>
As far as Oman's financial markets are concerned, fresh cross-border capital 
movement (inflows as well as outflows), and entry of new capital market 
instruments and banking products are just round the corner. This will drive the 
banks in the Sultanate to adjust their product-mix and effect changes in their 
operations to maintain competitiveness in the globalised settings. <br>
Globalisation will result in the entry of new foreign banks, and thus facilitate 
greater choice for consumers. Customers have already become more discerning and 
are compelling banks to offer a broader range of products through diverse 
distribution channels. The traditional face of banks as mere financial 
intermediaries has since altered and risk management has emerged as their 
defining attribute. <br>
The benefits of globalisation are being increasingly recognised by banks in the 
region. Integration of domestic and international markets has been facilitated 
by advancement in information and communications technology. However, such an 
environment has also meant that problems in one country can impact others 
instantaneously notwithstanding the strong fundamentals. <br>
Banks in Oman enjoy greater and highly competent supervision as the central 
banks in the region are continuously strengthening the accounting standards. 
There is a growing realisation that the ability of countries to conduct business 
across national borders and the ability to cope with the possible downside risks 
would depend, interalia, on the soundness of the financial system. This has led 
to the adoption of a strong and transparent, prudential, regulatory, 
supervisory, technological and institutional framework in the financial sector 
at par with international best practices. <br>
<br>
<b>The change constant </b><br>
All this necessitates transformation - in business processes and in knowledge 
management. This process is not a one-shot affair - it needs to be appropriately 
phased. The financial crises in recent years in emerging economies have 
demonstrated that, when things go wrong with the financial system, they can 
result in a severe economic downturn.<br>
The World Bank had observed that the combined loss of $1 trillion in banking 
crises during the 1980s and the 1990s is equivalent to the total flow of 
official development assistance to developing countries from the 1950s till 
date. As a result, the focus of reform in many emerging economies has not only 
been towards increasing efficiency, but also ensuring stability. <br>
From this 
perspective, financial sector reforms are essential to avoid such costs. It is, 
therefore, not surprising that financial market reform has been at the forefront 
of public policy debate in recent years. Indeed, it is by now widely documented 
that the structure of financial markets helps explain why some countries remain 
poor while others grow richer.<br>
<br>
<b>The reforms roulette</b><br>
Through the development of Bank for International Settlement Review January 
2003, an efficient financial system is thus perceived as a key element in 
raising countries out of their 'low level equilibrium trap'. As the World Bank 
observes, 'a robust financial system is a precondition for growth and the 
reduction of poverty'. <br>
Financial sector reforms were initiated in most emerging markets a decade ago to 
improve efficiency in the process of financial intermediation, thus enhancing 
the effectiveness in the conduct of monetary policy and creating conditions for 
integration of the domestic financial sector with the global system. <br>
The approach to various phases of financial sector reforms was to ensure that 
'the financial services industry operates on the basis of operational 
flexibility and functional autonomy with a view to enhancing efficiency, 
productivity and profitability'. <br>
In Oman and the Gulf, reforms have been focusing on strengthening the 
foundations of the banking system and bringing about structural improvements. 
The central banks in the Gulf, including Oman, have been addressing important 
issues such as ownership, corporate governance, regulatory and supervisory 
issues, retail banking, risk management technology, human resources development 
etc. <br>
One issue that a common AGCC banking forum will have to address in the immediate 
future is the interest rate scenario in the region. <br>
Another issue on which opinions have been dramatically changed in recent years 
is transparency. The transparency factor in Omani banks has been comparatively 
positive compared to other developing markets. However, public sector banks need 
to further discuss various issues and work hard to promote disclosures to 
maintain shareholder and market confidence factors and encourage open 
discussions. </font></p>
<p><font face="Verdana" size="1"><b>The importance of integration </b><br>
The success of European Union (EU) and the euro are 
greater examples for the Gulf to recognise the significance of integration. 
International banking experts suggest that AGCC banks set up a common forum and 
organise annual conferences for intensive discussions on both contemporary and 
futuristic issues facing the Gulf banking industry.<br>
The EU has altered the way financial institutions organise their daily business 
in the euro area. Integration is certainly and, naturally, most developed on the 
money market. Interest rate differentials across regional deposit markets have 
vanished and the &quot;law of one price&quot; applies to all transactions within a deep 
and highly liquid pan-European market. Cross-border transactions-for unsecured interbank deposits and repos-now account for over 50 per cent of all money 
market cash transactions. This deep integration of the money market has prompted 
new organisational patterns in the banking sector, with a concentration of Euro 
cash management activities making it possible to take advantage of higher 
liquidity levels on the secondary market. <br>
The integration of Euro-area financial markets has also deepened on the bond 
market: rapid internationalisation has taken place. Between 1998 and 2001, the 
relative share of corporate bonds issued by corporate borrowers in the Euro area 
as a percentage of GDP has more than doubled while it increased by one-third in 
the US. <br>
Internationalisation and competition between private issuers have been 
accompanied by an impressive surge in the amounts issued. Gulf banks have also 
been successful in issuing bonds: BankMuscat is one among those and is planning 
to come out with a public issue of bonds soon. Issuance of Euro denominated 
non-government bonds has more than tripled between 1998 and 2002. The 
integration of interest rate markets in the Euro area has been accompanied by a 
new phase in the development of derivative markets. The proportion of worldwide 
open interest contracts traded on European derivative exchanges went from less 
than 1 per cent to more than 15 per cent in 15 years.|<br>
<br>
<b>A framework for the future 
</b><br>
In all emerging markets including the Gulf, a sound and efficient banking and 
financial system is the key for economic growth. In many developing countries, 
significant progress has been made over the past few years in rehabilitating the 
banking sector and encouraging foreign ownership. The intermediation role of the 
banking sector remains fundamental for the efficient use of capital and 
sustained growth. Progress in corporate governance, the enhancement of the legal 
and supervisory frameworks that support the banking sector, and an efficient 
fight against money laundering are crucial too. <br>
Oman has already established a 
money laundering law. It is also welcome that Omani banks have started 
addressing topics such as capital structure (foreign partnership), corporate 
governance, risk management technologies and human resources development programmes to transform Gulf markets to one of the best financial markets in the 
world. Banks in Oman have to further strengthen the corporate set up with sound 
corporate governance. <br>
Yet, a precise definition of what is a relatively new 
concept remains blurred. Some take a narrow view, seeing &quot;governance&quot; as a fancy 
term for the way in which directors and auditors handle their responsibilities 
towards shareholders. Others expand the concept to explain a firm's relationship 
to society. Corporate governance is the system by which business corporations 
are directed and controlled.&quot; The corporate governance structure specifies the 
distribution of rights and responsibilities among different participants in the 
corporation and spells out the rules and procedures for making decisions on 
corporate affairs.</font></p>

