<p><font face="Verdana" size="1">'As a result of our sales, the business of each local agent has risen, and they 
receive more support from their principal agents in the Middle East because of 
their improving volumes,&quot; says T.J. Van Rooyen, general manager of Areej. <br>
By using the same sales tactics as those of duty free shops, Areej claims to 
lure more shoppers. 'Duty free offers customers the opportunity to reach the 
product and touch it - the barriers are broken down. Traditionally, stores in 
the region have counters preventing the consumer to interact with products 
directly and don't offer an enjoyable shopping experience,&quot; says Van Rooyen.<br>
The Areej outlets use bright lighting and clinical interiors. Shoppers are 
expected to find the stores fashionable and friendly. It has more floor space 
than a traditional perfume boutique, and there are more trained customer 
attendants on hand also. <br>
'Some of our customers may feel a bit intimidated at first when entering an 
Areej store which is so open and provides nowhere to hide from the beauty 
consultants in the store. But our staff are trained not to hound them and 
provide excellent service,&quot; says Van Rooyen. <br>
'We've also designed large beauty rooms because we respect the need for some 
element of privacy. Some people are happy to take treatment and advice on the 
floor, but many would like to do that in private,&quot; he adds. </font></p>

<p><font face="Verdana" size="1"><b>Follow the leader </b><br>
Retail and consumer patterns in the UAE's perfume market closely follow those of 
the Sultanate. Considerable potential local consumer spending on beauty products 
drifts towards the Emirates. The market is currently dominated by companies 
including Ajmal, Abdul Samad, Al Quraishi, Malik Al Oud, Al Nourani and Al 
Haramain. There are at least twice as many European brands than native brands in 
the market. <br>
'We have been able to stave off competition by ensuring quality and by promoting 
research and development&quot; says Abdulla Ajmal, in charge of business planning and 
international operations, Ajmal Perfumes. The company was set up 50 years ago in 
Dubai. Today, tens of Ajmal products offer stiff competition to leading European 
brands in perfume stores. The variety in price and selections in fragrances are 
mind-boggling. Ajmal has invested in a sophisticated R&amp;D division which is 
authorised to issue quality certificates on behalf of the Saudi Arabia 
Standardisation Organisation (SASO). <br>
The UAE market beckons brands from far and wide. Several months ago, the 
Brazilian brand O Boticario took a plunge into the highly competitive market in 
the UAE. They did not have any reason to regret the decision, shifting in excess 
of 40,000 beauty products in six months. The company boasts outlets in Brazil, 
Portugal, Japan, Bolivia, Peru and Paraguay and has a presence in Saudi Arabia 
and was planning to expand operation into other Gulf countries. <br>
Al Haramain Perfumes has more than 20 exclusive retail outlets in the Gulf 
market, of which 14 are located in the UAE. Headquartered in Ajman, it has 
manufacturing bases in Ajman, Bangkok, Laos and Cambodia. 'Our products, which 
offer fine blends of Arabian perfumes, are popular among Arab nationals. We have 
enjoyed a strong brand loyalty,&quot; says a company spokesperson. <br>
The dominance of Western brands in the region is increasing every year, 
according to V. Saji Kumar, brand manager, Grand Stores, Dubai. All the brands 
have cosmetic segments also, but perfumes rule the roost with a higher 65 per 
cent share in sales. 'The UAE nationals by and large prefer stronger perfumes to 
light ones. Incidentally, that was perhaps the reason why Rochas' Man Intense 
was a big winner in the UAE while flopping in the West,&quot; he says. <br>
At present, the most popular brands in Grand Stores across Dubai are Gucci-EDP, 
Dunhill Desire Blue and the ever-popular Chanel No. 5. Grand, which has eight 
showrooms in the UAE and supplies destination stores such as Paris Gallery. </font>
