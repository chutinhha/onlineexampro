                <td width="60%" valign="top"><font face="Verdana" size="1">
<b>Wealth of beauty </b><br>
The Middle East's beauty products market is estimated to be worth US$3.5 
billion. There is a vast shortage of shared sales data in the GCC, and no 
figures are available for Oman's perfume and make-up retail sector. However, 
there is growing evidence that the Sultanate is mirroring the growth patterns of 
the UAE in this segment. The perfume market is estimated to be worth around RO13 
million (US$33.8 million) where the demand for fragrances is forecast to grow by 
30 per cent by 2008. The sales are also helped by the fact that the UAE is 
developing as a re-export market for domestic as well as foreign brands. Volumes 
of perfume sales in the region fluctuate in seasonal cycles. For instance, 
Ramadhan and Eid promotions entice consumers, as do the various shopping 
festivals in the region. <br>
Until 20 years ago, the Gulf market was dominated by traditional Arabic 
perfumery that blended rare oils and scented spices to create products to match 
the user's skin type and personal preference. Since then, the GCC's economies 
through increasing affluence and trade liberalisation have evolved along with 
their perfume markets. The doors to beauty products from Europe and the US 
designed by fashion houses and lauded by Western celebrities have been flung 
open. <br>
'There is place for both Western and Arabic-made perfumes. What we've noticed is 
that Omani women tend to wear a combination of both,&quot; says an industry 
spokesperson. <br>
Whether foreign or locally made, it is mainly brand perception that decides 
perfume preferences. The region's consumers have become more sophisticated and 
increasingly discerning. They are perceptive to new and effective marketing 
communications and techniques. Omani perfume manufacturers have had to keep pace 
and spend more to enhance brand loyalty and also to popularise exclusive outlets 
in shopping malls. <br>
The outlook for the industry is that yet more emphasis will be made by 
manufacturers on brand-building while retailers will increasingly play a larger 
role in finding new ways to attract, and ultimately, sell those products to 
local consumers.<br>
                <br>
                <b>Retail innovation<br>
</b>Areej, a UAE-based perfume retailer, has brought to Oman its concept of 
delivering the broadest range of major international fragrances together in one 
location. This new approach has paid dividends, but Areej faced many 
difficulties when it set up shop in Muscat City Centre last year. The first was 
to convince the local agents of the leading perfume brands in the country to 
work in partnership with them. The local agents were reluctant to form such an 
alliance at first but, eventually, signed up for it. Today, their products are 
competing directly with each other under one roof. </font></td>
