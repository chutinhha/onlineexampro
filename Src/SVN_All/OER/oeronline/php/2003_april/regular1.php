            <font face="Verdana">Oil prices bounce back</font></font><p><b>
            <font face="Verdana" size="1">Oil prices nose-dived when US-led troops marched into Iraq, but prices soon 
rebounded, writes Richard Dean<br>
                  </font></b>
                  <br>
              <font face="Verdana" size="1">Oil prices rarely follow analyst 
forecasts, but the US-led invasion of Iraq was the exception that proved the 
rule. Economists confidently predicted crude prices would fall sharply as soon 
as the talking stopped and the fighting started. Sure enough, crude prices 
tumbled. Dated Brent peaked at US$34.93 per barrel on March 7. By March 21, just 
three days after the outbreak of war, it closed at US$25.08. <br>
So three cheers for the analysts: they guessed correct that uncertainty was 
supporting prices, not the threat of conflict itself. But their work is far from 
done. Indeed, the difficult bit is just beginning. A quick glance at the oil 
market history books made it crystal clear that prices would collapse in the 
short term - that's exactly what happened when Stormin' Norman Schwarzkopf fired 
the starter pistol for operation Desert Storm in 1991. <br>
The real trick is forecasting what happens next. And here, history is of little 
use. Now that battle has commenced, fundamental forces of supply and demand come 
back into play. Without the past to guide them, analysts are back gazing into 
their crystal balls.<br>
&quot;Of course, the big question is: what will prices do in the near future?&quot; says 
the Energy Information Administration (EIA), part of the US Government's 
Department of Energy. &quot;As always, that's a difficult question to answer.&quot; <br>
Evidence points to prices shooting back up sooner rather than later. Energy 
analysts at Standard Bank in London warned straight away that positive sentiment 
in oil markets had gone too far. &quot;The war has started and the markets have been 
reacting with a sell off in crude and products as traders feel that the conflict 
in Iraq will almost certainly be a short victory for the allied coalition,&quot; said 
a report published on March 20. <br>
&quot;Although we are seeing the war premium being eroded, it would be premature to 
assume that this is the beginning of the much anticipated price collapse.&quot; 
Specifically, US oil inventories in the week ended March 21 were 14 per cent 
lower than a year earlier at 273.9 million barrels, according to the U.S. Energy 
Department. <br>
&quot;Although WTI and Brent both are trading under $30 per barrel, we believe that 
this is only short term and that prices will gain upward momentum before the 
conflict is over,&quot; said the bank.<br>
By March 31, their scepticism looked well placed. Dated Brent had recovered some 
$3 per barrel in a week of fighting, to $28.08 per barrel. The International 
Energy Agency, the developed world's energy watchdog, is also cautious about the 
oil price dip. In its March Monthly Oil Market Report, the agency warned that 
the world was facing an oil shortage in the event of a war in Iraq. It said 
markets were &quot;running on empty&quot; in early March. OPEC heavyweights such as Saudi 
Arabia, Kuwait and the UAE were already producing close to capacity, following a 
strike in Venezuela that slashed the South American country's exports in 
December. Since then, Venezuelan exports had come back on stream, but Middle 
East OPEC producers had kept their taps open in a bid to replenish depleted 
inventories. At the same time, civil unrest in Nigeria was cutting output by 
about one-third. <br>
The combined effect, in the IEA's view, was that OPEC was in no position to plug 
the Iraqi gap. &quot;A further supply disruption would tax a system operating at 
close to capacity,&quot; said the Agency. Before the fighting started, Iraq was 
exporting around 2 million barrels per day of crude, out of global consumption 
of some 79 million barrels per day. <br>
The IEA, based in Paris, often disagrees with the EIA, based in Texas - 
particularly on the issue of the global supply/demand balance. The fact that 
both agree that supply shortfall is a genuine concern hammers home the 
seriousness of the situation. <br>
&quot;More crude oil supply appears necessary in order to produce more refined 
products, and also to increase crude oil inventories,&quot; says the American body. 
&quot;Although Venezuelan production started to increase by late February and Saudi 
Arabia, as well as other OPEC countries, reportedly increased production in 
January and February, the United States has yet to see a lot of this increased 
oil production.&quot; Throw in the fires that are burning in Iraqi oil fields as 
Saddam Hussein orders his troops to light some Iraqi oil wells, and the spectre 
of long-term supply disruption looms extremely large. <br>
So why did oil prices fall so sharply at the outset of war, only to rebound 
within a week? None of the supply information was secret - oil traders simply 
ignored it. <br>
Oil demand holds the key. In the&nbsp;&nbsp;&nbsp;&nbsp; medium term, many 
traders still believe demand will fall sharply in coming weeks, for two reasons. 
First, the northern hemisphere winter is drawing to a close, leading to a steep 
cut in demand for heating energy. Second, they believe war will deal a hefty 
blow to the global economy, which will in turn hit oil demand. <br>
They are also optimistic that supply-side fears are overstated. Crucially, Saudi 
Arabia has pledged to make up the Iraqi shortfall in coming weeks and months. 
Some observers aren't convinced Saudi has the capacity to do this. But most oil 
traders appear to believe Ali al-Nuami, Saudi Arabia's oil minister, who is 
adamant that the Kingdom will satisfy the world's thirst for crude. <br>
&quot;The Kingdom of Saudi Arabia will work to implement the resolutions of the 124th 
conference of the Organisation of Petroleum and Exporting Countries, which 
stressed the organisation's readiness to make available the supplies, hoping 
that this could contribute to the stability of the market, and assuring the 
historical role of the Kingdom and member countries of OPEC to ensure supplies 
to the world under any circumstances,&quot; he said in the wake of the US-led war.
<br>
For all these reasons, many traders are betting there will be too much oil on 
the market in April and May, and that prices will fall. But after weeks of 
speculation that sent prices far too high, some observers believe traders went 
too far the other way. Expect prices to hover around $30 per barrel, at least 
for the next few weeks.</font></p>
