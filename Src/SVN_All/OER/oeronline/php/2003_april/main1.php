                      </b> </font><font face="Verdana" size="4" color="#FF0000">Securing 
                      finance</font><font face="Verdana" size="1"><br>
                      <b>Any project being financed in Oman will attract a number of legal issues. Imtiaz 
Shah highlights some vital issues and their solutions </b><br>
                      <br>
                      Over the past few years, the Sultanate 
has been one of the region's most active markets for projects financed on a 
limited recourse basis. The Manah Power project, Oman LNG, the recent 
independent power projects and the financial conclusion of the Oman India 
Fertiliser project mark a successful track record of major investment 
initiatives banked in Oman.<br>
Each of these projects has involved large sums of money advanced by a syndicate 
of lenders to the project company. The lenders in each case required robust and 
comprehensive security over the assets of the project company to secure their 
interests in the project. The categories of security available to lenders in 
Oman include: mortgage over land; mortgage over the commercial registration of 
the project company and its assets; mortgage over shares in the project company 
and over other securities; and assignments of receivables.<br>
Each of these categories has certain limitations in relation to the types of 
assets that are chargeable. The following assets are probably not chargeable as 
a matter of Oman law: </font>
<ul>
  <li><font face="Verdana" size="1">A lease over land </font></li>
  <li><font face="Verdana" size="1">Future contingent rights </font></li>
  <li><font face="Verdana" size="1">Partners' interests in an LLC</font></li>
  <li><font face="Verdana" size="1">Fluctuating sums in bank accounts
  </font></li>
</ul>
<p><font face="Verdana" size="1">Further limitations on lenders' recourse 
exist in the law relating to the enforcement of security in the Sultanate. The 
basic rule is that security is enforced through a court order, by way of a 
public auction. It is, however, possible for the lender to ask the court to 
transfer the assets directly to him. <br>
A mechanism that has been suggested by lenders on a number of project financings 
in Oman is the sale and purchase of assets. This comprises an agreement (or 
series of agreements) between the borrower and the lenders' agent under which 
the borrower grants a put option in favour of the lenders exercisable in the 
event of a default. <br>
<br>
<b>Government support <br>
</b>The level of government support that is made available to a project may 
directly impact the project's financial viability. Consequently, most sponsors 
and lenders to recent Omani projects have, to a greater or lesser extent, 
requested the government to support their projects. Examples of the type of 
support that has been sought includes:</font></p>
<ul>
  <li><font face="Verdana" size="1">Tax exemptions for five or 10 years
  </font></li>
  <li><font face="Verdana" size="1">Exemption from Customs duties during 
  the construction phase </font></li>
  <li><font face="Verdana" size="1">Waiver of fees payable on registration 
  of security or on transfer of the shares </font></li>
  <li><font face="Verdana" size="1">Waiver of government priority as a 
  creditor </font></li>
  <li><font face="Verdana" size="1">Confirmation that no changes in the 
  law will adversely affect the project</font></li>
  <li><font face="Verdana" size="1">Technical exemptions from certain 
  legal provisions </font></li>
</ul>
<p><font face="Verdana" size="1">Sponsors and lenders will usually prefer 
to have some support in the form of a Royal Decree. While a majority of projects 
in Oman have enjoyed this benefit, recent projects have not had such support, 
thus proving that a decree is no longer a pre-requisite for international 
lenders given Oman's strong track record in implementing projects. <br>
<br>
<b>Equity Support </b><br>
On virtually all projects, lenders will expect the sponsors to contribute equity 
to help fund the project. The extent of such equity will depend on the 
creditworthiness of the sponsors and is usually in the region of between 20 and 
30 per cent of the total project costs.<br>
In Oman, the project company has been either a limited liability company (LLC) 
or a closed joint stock company (SAOC). Particularly in the case of the latter, 
equity is usually provided by way of the sponsors increasing the share capital 
and subscribing for new shares issued by the project company. <br>
In the local context, two particular issues arise. First, the lenders will 
require the equity contributed to be in the 'project currency' (usually US 
dollars) and for it to be placed in an offshore equity reserve account, given 
the uncertainty over bank accounts in Oman. The company may find that the 
Ministry of Commerce &amp; Industry (MCI) requires proof that such capital has been 
contributed into a bank in the Sultanate in Omani rials, as this will be the 
denomination of the shares. <br>
This can be dealt with in a number of ways: ministry exemption, maintaining an 
equity reserve account onshore or treating the contribution in US dollars as a 
contribution in kind of the foreign currency. A second issue arises where 
sponsors contribute equity at different times. Any intervening exchange rate 
movement could result in disparity between the agreed sponsor shareholdings. 
This can be resolved by equalisation language in the equity. <br>
<br>
<b>Project insurances </b><br>
The creation and maintenance of appropriate and adequate insurances over the 
project assets will be a major concern for the lenders, as the acquisition of 
the project assets will primarily be funded from their debt finance. In Oman, 
there are restrictions on the extent to which assets here can be insured and/or 
reinsured by insurance companies outside the country. <br>
In practice, partial waivers are obtained from the MCI so that the project is 
insured in Oman but reinsured abroad. <br>
<br>
<b>Foreign capital licences</b><br>
The major lenders on significant projects in Oman have been foreign banks with 
no business presence in the country. This may create difficulty on enforcement 
as such banks will be unable to hold certain assets transferred to them on 
enforcement, in particular interests in land or shares.<br>
In practice, the lenders will appoint an Omani bank as their 'onshore security 
agent' to hold the benefit of security on their behalf. The role of such an 
agent is set out in some detail in an onshore security agency agreement. <br>
A further issue arises where one or more foreign sponsors have contracted to 
provide services to the project company. E.g., these could be in the form of 
manpower, or technical expertise in operating a particular facility. Here, to 
comply with the Sultanate's foreign capital investment law, such a sponsor will 
generally form (with an Omani partner) a local company or, depending on the 
nature of the project, it may be able to establish a fully-owned branch office 
to carry out such services. <br>
<br>
<i>Imtiaz Shah is a Senior Solicitor with 
Denton Wilde Sapte, Muscat </i> </font></p>
