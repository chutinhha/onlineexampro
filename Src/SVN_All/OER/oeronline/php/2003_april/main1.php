            <font face="Verdana">A big deal</font></font><p><b>
            <font face="Verdana" size="1">OER talks to Anthony Smith, OMIFCO's 
            chief executive, who reflects upon the challenges that were overcome 
            to secure a better deal for the Oman India fertiliser project </font>
            </b></p>
<p><font face="Verdana" size="1">Although the delay in attaining financial 
closure had tested the nerves of many involved, the finance scheme may have 
benefited the Sultanate in developing the US $969-million Oman India fertiliser 
project. Today, this remarkable project-the largest investment project 
undertaken by Indian State-run companies outside of India-is on track to 
commence in July 2005. Apart from showing the world that Oman can develop 
mega-industrial projects, the fertiliser plant demonstrates that the country can 
acquire international limited recourse project financing without diminishing its 
international credit rating. <br>
&quot;The final project agreement structure insulates the project from product market 
price volatility, which helped to gain financing,&quot; says Anthony Smith, the CEO 
of Oman India Fertiliser Company (OMIFCO) SAOC, the organisation developing and 
managing the fertiliser project. <br>
He explains that a problem with the fertiliser project's original structure was 
that not only was it complicated, but downward price movements in the urea 
market would also have adversely affected the project. <br>
Although the original structure seemed reasonable at the time of conception, 
while attempting to close financing in 1999, both urea and ammonia market prices 
were depressed and far below the proposed urea floor price. This would have 
activated subordinated loans from the Gas Supplier and Indian partners involved 
in the deal. To bring back confidence to the financing players, it was decided 
to restructure the project to make it more robust and bankable, recalls Smith.
<br>
The Indian government agreed to off-take the entire urea production at 
pre-determined fixed prices and were prepared to mitigate the market price risk. 
Pre-determined fixed pricing has insulated the project from the uncertainty of 
unpredictable market prices. It must be noted that, in the recent past, urea 
prices have been volatile. <br>
The process of formulating the urea pricing, getting the Indian government to 
approve it, and then recasting project agreements was completed by end-2001. 
During this time, one of the original Indian sponsors of the fertiliser project 
was replaced. <br>
&quot;OMIFCO then encountered more or less standard project financing difficulties,&quot; 
confirms Smith. Project agreements developed among the various parties were 
subject to due diligence and changes required by the lenders. There were 
protracted negotiations on the terms of the finance agreements. The export 
credit agencies also came up with a list of changes they wanted.<br>
The second financing campaign started in March 2002, when the invitation was 
sent to the syndicating banks. The deal was well received by the banks, and 
comfortably over-subscribed. In July 2002, the parties concerned signed all the 
finance documents. Finally, there were about 260 conditions precedents attached 
to drawing down the loans. The procedure to satisfy these conditions took a huge 
effort on the part of the owner, lawyers and lenders. Successful financial 
closure was achieved on October 9, 2002. <br>
Not surprisingly, 9/11 presented OMIFCO with some difficulties. It made the 
finance markets a bit jittery. It caused turbulence in the insurance market, and 
premiums rose dramatically as a result. But these difficulties were also 
overcome. <br>
&quot;Personally speaking, I think there is plenty of finance available for well 
structured projects in Oman, but lenders are, in the current climate, examining 
risks more carefully,&quot; Smith adds. <br>
Overall, the delays had a positive impact on the fertiliser project in two ways. 
Firstly, the preferred EPC contractor nominated in 1997 was selected on the 
price it had quoted in 1996, without any escalation due to the delay. <br>
The project also gained from reduced interest rates. &quot;Had we gone ahead in 
1998-99, the interest rates would have been much higher. Although there was some 
increase in the owner's pre-closing costs because of the delay, there was a 
reduction in interest rates and no overall negative impact,&quot; says Smith. </font></p>
<p><font size="4" color="#FF0000"><font face="Verdana">Lending support</font></font><font face="Verdana" size="1"><br>
<b>The eagerness of lenders to arrange large-scale finance in Oman looks set to 
continue in the long term. OER reports.</b></font></p>
<p><font face="Verdana" size="1">A major challenge facing banks and export credit agencies (ECAs) during the last 
decade has been to meet the demand for structured finance to support an 
increasing number of privately-funded projects in the world's developing 
economies, particularly the Middle East. In the May 2002 issue of OER, 
Rajshekhar Singh, project finance head, BankMuscat, detailed the factors why 
international lenders have relented in their enthusiasm to eye limited recourse 
project finance opportunities in the region. Although he admits that the 
prospects of a prolonged war in Iraq could potentially dry up sources of finance 
in the short term, he says the outlook remains positive. Indeed, there's over US 
$4.0 billion of local project financing transactions in the pipeline. <br>
&quot;There is enough appetite among the international lending community for project 
finance transactions in the country although there may be some banks in the 
local market that may choose to, instead, focus on retail banking among other 
strategy areas,&quot; says Singh who reaffirms that BankMuscat will continue to play 
a key role in arranging project finance in Oman. <br>
The reason for this optimism is the number of deals being brought to market as 
well as the increase in the volume of finance being arranged through commercial 
loans. He cites the example of power projects concluded in 2001 and Oman LNG's 
refinancing initiative concluded in early 2002, which attracted several 
international and regional banks. Singh also notes that the tenors for project 
financing, especially in the power sector, have lengthened, and the pricing for 
project deals has become finer. <br>
A lender looks at many project-related risks when determining the level of loan 
it will commit. These factors include the project's commercially viability, 
size, and level of debt. Omani banks are limited by how much money they can lend 
while single-borrower constraints ensure prudence is maintained. Regulatory 
norms advised by Central Bank of Oman limit local banks to lend up to 15 per 
cent of the bank's net worth to a single entity or group. There are many 
benefits attached to becoming a lead arranger on an investment project apart 
from the interest income. Firstly, assuming the project is prestigious, it can 
lift the profile of the lender. Also, lead finance arrangers usually generate 
additional revenue from arranging fees and syndication charges from the project 
management company. Moreover, some of these projects may be large, employing 
several hundred people, and it may have spin-off benefits including being a 
preferred banker to the company's employees.</font></p>
