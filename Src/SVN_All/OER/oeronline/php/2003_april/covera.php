                    Cover Story<br>
                    </font><font face="Verdana" size="4" color="#FF0000">A tale of two 'technology' cities</font><p>
            <font face="Verdana" size="1"><b>KOM is taking on Dubai's Internet City in a bid to transform Oman into the 
technology capital of the Middle East, says Richard Dean </b></font></p>
<p><font face="Verdana" size="1">Oman has launched an audacious bid to 
become the regional technology hub. The government says knowledge-based 
investment hold the key to Oman's economic and social development. Ministers 
hope the Knowledge Oasis IT park in Muscat will act as the catalyst for this 
bold project. <br>
Spearheaded by HE Maqbool bin Ali Sultan, the Ministry for Commerce and Industry 
has worked with global consultants to draw up a wide-ranging strategy to 
transform Omani economy. <br>
Specifically, HE Sultan's wish list includes educational reform, a comprehensive 
ICT policy, technology universities, technology parks and business incubators, 
and pro-business laws. &quot;A fundamental shift is required from raw material 
processing and manufacturing to a knowledge-based economy.&quot; <br>
KOM forms the cornerstone of this ambitious policy. The park has been designed 
to meet the perceived needs of leading technology players with industry standard 
infrastructure. Crucially, the Oasis will enjoy free zone status, with 100 per 
cent foreign ownership and zero taxation. <br>
&quot;There is a substantial potential in Oman for developing software for sectors 
such as banking, telecommunications, government's programme of e-governance, 
IT-enabled services and call centres, providing a source of qualified IT 
professionals for the entire Gulf,&quot; says HE Sultan. The race is on to attract 
international investors; the government claims German technology giant Siemens 
has agreed to set up a state-of-the-art laboratory for testing software. </font>
</p>
<p><font face="Verdana" size="1"><b>The Dubai factor</b><br>
Launched in 2000, Dubai Internet City (DIC) has already established itself as 
the regional centre for the global IT industry. Microsoft, Oracle, IBM, Cisco... 
DIC's tenant list reads like a who's who of the technology world.<br>
In February, Sony Ericsson Mobile Communications opened its Middle East and 
Africa headquarters at DIC. The new office will serve as the company's regional 
hub for the entire Middle East and Africa region, covering more than 60 
countries. <br>
&quot;With its modern infrastructure, excellent communications and transportation 
links, Dubai offers the ideal location for our regional hub,&quot; explains Mats 
Paulson, GM (Middle East and Africa), Sony Ericsson. &quot;Basing ourselves here will 
better enable us to reach out to our fast-growing client base in the region,&quot; he 
said. <br>
DIC is in the grip of a virtuous circle. Management made the shrewd move of 
securing 'anchor' clients before launching. When plans for DIC were unveiled in 
1999, Microsoft and Oracle were at the launch party, endorsing the project. That 
bought DIC enormous credibility. Others followed, and DIC now boasts of a strong 
technology community. <br>
&quot;In DIC, we have a community of great diversity and talent,&quot; says Dr Omar Bin 
Sulaiman, CEO of DIC. &quot;We have businesses and knowledge workers from across the 
[technology] industry and around the world.&quot; <br>
KOM might struggle to compete with an isolated Internet City, but Internet City 
itself is only half the battle - Dubai's status as the region's commercial 
heartland is a further obstacle. <br>
Dubai has created robust economic clusters around its free zones and spared no 
cost in ensuring that these are at the cutting edge in terms of infrastructure 
and strategic positioning,&quot; says Dubai Development and Investment Authority (DDIA).
<br>
Clustering is crucial, says DDIA. &quot;These knowledge clusters provide significant 
efficiencies and competitive advantages to companies and have been key in 
transforming Dubai into a knowledge economy.&quot;<br>
OER contacted a number of leading regional information technology figures to ask 
their opinion of KOM, and its prospects for competing with Dubai. None would 
comment on the record. But one regional manager with a leading international 
technology and media company said the argument was clearly in favour of Dubai.
<br>
&quot;Everyone's in Dubai. It's as simple as that. If you're in Dubai, all your 
technology partners are at your doorstep. Another big factor is staff. It's much 
easier to hire qualified people in Dubai. It has that reputation,&quot; he says. <br>
He adds: &quot;Muscat's a nice place, but it's missed the boat. This is a fairly 
small region, and there's room for only one IT hub. That hub is Dubai.&quot; <br>
That's not to say international technology giants ignore Oman. Microsoft has an 
office in Muscat, as do most of its peers. But Muscat bureaux are sales offices. 
Dubai is home to regional management, strategy formulation and even some product 
development (Microsoft uses its Dubai base to help develop its Arabic operating 
systems). <br>
So what does the future hold for KOM? The most likely route is that it will 
emerge as an incubator for local IT start-ups. Indeed, HE Sultan stresses that 
the local market is vital. &quot;KOM is a complete IT planet, opening a new galaxy of 
opportunities for entrepreneurs, IT professionals and Omanis.&quot;<br>
&quot;KOM as an idea is very good. Bringing the intellectual capacities of people 
together in a techie environment will result in a lot of value addition,&quot; 
believes Andre Podgaietsky, general manager of Gulf Business Machines Oman. <br>
If it succeeds in carving out a successful niche in this area, it will have 
eclipsed Dubai in one core area: innovation. &quot;Dubai was touted as a centre for 
R&amp;D, but in reality, it is just a tax-free distribution centre for 
multinationals,&quot; says the regional IT manager. If KOM fosters a genuinely 
entrepreneurial culture among young Omanis, the value to the country will be 
immense. <br>
&quot;Oman offers one of the moist favourable business climates for IT investment and 
growth in the region,&quot; believes Jawal Al Redha, co-chairman (Middle East), 
Business Software Alliance. &quot;The steps being taken by the government are 
encouraging foreign investment and boosting the IT industry in Oman as they 
clearly underline the Omani government's commitment towards supporting both 
local and global IT industry,&quot; he adds.</font></p>
                    <hr color="#CC3333">
<p><font face="Verdana" size="4" color="#FF0000"><a name="PC">PC</a> ownership: an opportunity
</font><font face="Verdana" size="1"><br>
<b>There are only 97,000 computers installed in Oman. This is good news for vendors 
that are hopeful of doubling the country's computer ownership within five years.
</b>
</font></p>
<p><font face="Verdana" size="1">
<img border="0" src="graph-2.jpg" align="left" hspace="3" vspace="3" width="200" height="214">Personal computer (PC) ownership in Oman 
is far below the world average, according to the Dubai-based research company 
Madar. It reveals that PC penetration in the Sultanate is only 3.51 per cent as 
compared to a world average of 10 per cent. Far from viewing this as negative, 
below world-average PC penetration in the region presents an opportunity for 
vendors. Recent developments indicate that PC penetration is almost certain to 
increase at a dramatic pace, fuelled by factors like rising PC literacy and 
government-led IT initiatives across the region, including KOM. Moreover, the 
momentum is set to pick up tremendously in the next few years as PCs become more 
affordable. <br>
&quot;The low PC penetration in the Arab world signals an immediate acceleration in 
the demand for PCs because of the dynamism displayed by individuals, companies 
and governments to embrace IT,&quot; says Dirk de Waegeneire, vice-president 
(international sales), Fujitsu Siemens Computers. &quot;There is room for everybody,&quot; 
feels Gulf Business Machines Oman's Andre Podgaietsky.<br>
The overall PC penetration in the 18 Arab states covered by Madar's research 
stood at 2.22 per cent at the end of 2002, with rates ranging between 0.5 per 
cent and 14.8 per cent. Indeed, Pan-Arab PC penetration continues to be well 
below the world average, with the exception of GCC states Bahrain, Qatar, UAE 
and Kuwait, which recorded rates of 14.8, 13.4, 13.2 and 12.4 per cent, 
respectively. The robust activity in the IT markets of Saudi Arabia and the UAE, 
which together constitute over 80 per cent of the GCC's entire IT market, look 
set to continue. The third major player is Bahrain, which has allocated a huge 
budget for IT, above the world average.</font></p>
<p><font face="Verdana" size="1"><b>Buying technology </b><br>
When viewed as a percentage of Gross Domestic Product (GDP), Bahrain tops the 
list with 3.97 per cent against the world average of 3.88 per cent, followed by 
Saudi Arabia (1.99 per cent) and UAE (1.77 per cent). Oman's IT-spend as a 
percentage of GDP stands at 1.02 per cent. <br>
&quot;The expanding GCC market, which stood at US$6 billion in 2002, is poised to 
record striking growth within the next three years, fuelling demand for proven 
solutions for enterprise applications and systems integration,&quot; says Diyaa 
Zebian, regional manager of eSolutions at BEA Systems, Inc. This optimistic 
forecast is based on market research projections that indicate that the GCC IT 
market value will touch US$8 billion by 2005. <br>
&quot;The bright forecasts in IT growth are supported by massive investments being 
made by key GCC governments keen to boost their non-oil revenues by moving to 
the Digital Economy,&quot; said Zebian. &quot;The governments are often the driving force 
in this IT expansion, and that is one reason why IT growth is higher in the Gulf 
than some of the developed countries. This drive is impacting businesses as well 
as the community, indirectly accelerating the region's march towards the Digital 
Economy,&quot; adds Zebian.</font></p>

