We do not work within the realms of a strict hierarchical structure.</font></p>
<p><font face="Verdana" size="1"><b>Leadership style </b><br>
I believe in working as a team. Our organisation is one large family. I 
personally interact with all the staff members and keep them motivated. I do not 
thrust my views or decisions on the team. In fact, no decision is without 
consulting the people concerned. I like having people who are pro-active and 
enthused about their work - so, I encourage initiative and lend a willing ear to 
any staff member with an interesting idea. </font></p>
<p><font face="Verdana" size="1"><b>Biggest strength </b><br>
