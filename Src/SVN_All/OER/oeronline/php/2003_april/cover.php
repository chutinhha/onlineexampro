            Knowledge Oasis Muscat<br>
            IT is Here</font><p><b><font face="Verdana" size="1">
            <img border="0" src="cover-2.jpg" align="left" hspace="3" vspace="3" width="290" height="148">&quot;It has been a 
            dream for us here in Oman to join the global IT revolution,&quot; says HE 
            Maqbool bin Ali Sultan, Minister for Commerce and Industry. <br>
            Right now, the wheels in the government are moving fast to make this 
            dream a reality. The administration is backing up its vision for 
            transforming Oman from an oil-based economy to a knowledge-based one 
            with more than just sound bytes. <br>
            As you read this, finishing touches are being provided to the 
            multi-million-dollar complex that is Knowledge Oasis Muscat (KOM), 
            and top IT businesses are planning to base their operations there.
            <br>
            The IT Park has been designed to meet the needs of leading 
            technology players, and the government envisages that a thriving 
            technology hub in Muscat would invigorate the national economy. But 
            is it a realistic goal? </font></b></p>
<p><font face="Verdana" size="1">In an exclusive interview with Oman 
Economic Review, HE Ali Bin Masoud Al Sunaidy, Undersecretary, Ministry of 
Commerce and Industry, outlines the vision behind the Knowledge Oasis, charts 
the progress at the technology park, and explains how it is set to revolutionise 
the IT industry in the Gulf region. Excerpts: <br>
<br>
<b>Oman Economic Review:</b> What is the 
philosophy behind KOM? <br>
<b>HE Ali bin Masoud Al Sunaidy: </b>KOM is a public-private sector-led initiative 
situated on a 68-hectare eco-friendly site of natural beauty located 32 km north 
west of Muscat, and on the doorstep of Rusayl Industrial Estate and Sultan 
Qaboos University (SQU). More than 12,000 sq. mt. of office and incubator space 
and two leading private IT colleges have already been completed, and a further 
phase of development is at present underway. KOM will accommodate every size of 
business, from a single entrepreneur to major international research facilities, 
call centres and business headquarters. Indeed, KOM is totally committed to 
creating an environment in which entrepreneurs, researchers, and small- and 
medium-sized enterprises (SMEs) as well as established multinationals can 
innovate and flourish within a Gulf setting. <br>
KOM's mission is to be a hub of Information and Communication Technology (ICT) 
excellence, one that advances human and social development through the 
generation, dissemination and application of knowledge using Oman's competitive 
advantages. We'll achieve this by carrying out a series of initiatives that 
include: attracting not only blue chip companies, but knowledge and 
technology-driven SMEs; offering premises on flexible terms which relate to the 
high risks and uncertainties of small embryonic companies; providing world-class 
facilities and support services for businesses that are developing or trading in 
innovative products and services; establishing an incubator programme where 
start-up businesses can be nurtured; and stimulating an increased rate of 
interaction between Oman-based ICT academic institutions, SQU and knowledge and 
technology-driven businesses.<br>
I also see KOM playing a key role in helping nurture a domestic culture of 
innovation and entrepreneurship, something that's important if we're to 
encourage youngsters to pursue careers that require a background in maths, 
science, engineering and technology.<br>
In brief, KOM is intended to be a multidisciplinary park, a place where 
high-tech, knowledge-based firms can thrive - a place where entrepreneurs, 
policy-makers, business people, students and researchers interact on a daily 
basis. KOM's remit is very clear: enhance and nurture Oman's position as a 
regional centre of business excellence, innovation and entrepreneurship. <br>
<br>
<b>OER:</b> What kind of investments have been 
made, or are to be made, at KOM? <br>
<b>HE Sunaidy:</b> The government has invested over RO4 million, and two private IT 
colleges have placed a further RO2 million. Omantel is making large, strategic 
investments in KOM. The first is an Omantel-Siemens test lab worth RO2.7 
million. The second is a data centre of around RO1 million. Through 
contributions from the government, Omantel, the Park's IT colleges and other 
private parties, we're looking at an initial investment of close to RO10 
million. <br>
<br>
<b>OER:</b> Why should a company come and invest 
in KOM?<br>
<b>HE Sunaidy:</b> Without doubt, KOM's property and land are certainly its most 
tangible features. Tenants will enjoy the peace of mind that comes when 
everything has been taken care of, they simply have to arrive and plug in. 
They'll be able to occupy an office, a floor or a building (typically three 
floors). The Park will have a university campus-business village atmosphere, 
making it a unique setting in which businesses can build and grow. KOM will 
offer the following opportunities: </font></p>
<ul>
  <li><font face="Verdana" size="1">A prestigious address and highly 
  pleasant and eco-friendly location with adequate car parking </font></li>
  <li><font face="Verdana" size="1">Excellent communications, both 
  electronic and physical; KOM is just 10 minutes drive from Seeb Airport </font>
  </li>
  <li><font face="Verdana" size="1">State-of-the-art heating ventilation &amp; 
  air conditioning (HVAC) offices</font></li>
  <li><font face="Verdana" size="1">High bandwidth up to 155 Mbps </font>
  </li>
  <li><font face="Verdana" size="1">Use of KOM's Class-A conference rooms 
  and audiovisual equipment </font></li>
  <li><font face="Verdana" size="1">Opportunity to bounce ideas off 
  experienced business personnel and researchers, as well as devise 
  collaborative research and development (R&amp;D) programmes and receive expert 
  advice and consultancy </font></li>
  <li><font face="Verdana" size="1">Networking, in partnership with 
  international organisations, professional bodies and tertiary institutes, KOM 
  will organise and host events and executive roundtables to share ideas, 
  challenges and referrals. <br>
  In addition to the advantages of the country's strategic location, political 
  stability, infrastructure, economic resources and market-oriented policies, 
  there are a number of incentives offered to tenants, and these are: </font>
  </li>
  <li><font face="Verdana" size="1">100 per cent foreign ownership </font>
  </li>
  <li><font face="Verdana" size="1">Highly competitive telecom rates</font></li>
  <li><font face="Verdana" size="1">No personal income tax and no foreign 
  exchange controls</font></li>
  <li><font face="Verdana" size="1">Zero tax and import duty exemptions 
  for eligible companies </font></li>
  <li><font face="Verdana" size="1">Eligible companies will be exempt from 
  income tax for five years, and renewal for a further five years </font></li>
  <li><font face="Verdana" size="1">Duty-free access of products from Oman 
  to GCC countries</font></li>
  <li><font face="Verdana" size="1">Determined policy of privatisation
  </font></li>
  <li><font face="Verdana" size="1">Stable, fully convertible currency
  </font></li>
  <li><font face="Verdana" size="1">Price stability - with an inflation 
  rate not more than 1 per cent since 1992 </font></li>
</ul>
<p><font face="Verdana" size="1">Our incentive scheme for land and 
buildings is also highly attractive, offering leases of up to 99 years. 
Moreover, the cost of land at KOM is competitive - as low as 150 baisas to 250 
baisas per square metre per year whereas one may have to pay as much RO6 per 
square metre per month elsewhere. Given the nature of KOM and the incentives and 
opportunities it offers, we're also keen to encourage the private sector to come 
and build their office blocks here. <br>
We're aware that advances in telecommunications and transportation mean that 
enterprises in the knowledge economy can be located just about anywhere around 
the globe. However, tomorrow's company is concerned with the skills, innovation 
and energy of its people. It is, therefore, attracted to locate in stable 
communities that offer a healthy, educated, innovative, creative, adaptable and 
contented workforce. It is also attracted to locations that are part of 
community life. On these fronts, Oman has a real competitive edge. <br>
KOM will also offer a portfolio of business support services and facilities 
targeted at the needs of its tenants. Indeed, working collaboratively is 
integral to KOM's philosophy and culture, and the Park's Business Mentor 
Programme (BMP) is intended to connect KOM residents with industry experts who 
are willing to share their knowledge. The programme already has a growing list 
of professionals who have committed to helping make companies at KOM successful.
<br>
<br>
<b>OER:</b> How is KOM expected to shape up? <br>
<b>HE Sunaidy:</b> KOM will certainly be one of the main driving forces behind the 
development of Oman's ICT sector. It will influence and change the way people do 
business here. Moreover, it'll encourage them to adapt to the electronic medium. 
As outlined earlier, KOM will boast state-of-the-art infrastructure, and once 
the Park becomes fully operational, I envisage a host of businesses operating 
here, all adding tremendous value to the economy as well as to the country's 
social development. <br>
<br>
<b>OER:</b> Could you throw some light on the 
relationship between KOM and Omantel? <br>
<b>HE Sunaidy:</b> Our partnership with Omantel is a rare occurrence. One doesn't 
usually see a major telecom company partner an IT park. We feel this is a move 
in the right direction. Omantel is not a client but a fully-fledged partner in 
helping us take KOM on and up. I'm delighted with their involvement in such an 
important and prestigious national project. <br>
<br>
<b>OER:</b> How would you compare KOM with other 
IT parks in the region? <br>
<b>HE Sunaidy:</b> In a globally competitive environment, comparisons will always be 
made. This is unavoidable. But it's important to remember that different IT 
parks exhibit different characteristics due to the environments in which they 
are set up and the actors involved. <br>
I expect many of KOM's early tenants to be nimble though globally-focused SMEs. 
The needs and problems of these firms are specific, and in today's business 
environment, they're under pressure. While everybody's telling them to get into 
e-commerce and 'go global', not many tell them how to do it. This is where KOM 
can play a pivotal role. Quite simply, KOM has been designed to get firms into 
the market, not protect them from it. From my perspective, such a collaborative 
approach to nurturing and developing tenants is relatively unique in the region.
<br>
Over the years, most IT parks have been developed as a means of stimulating 
start-ups and the growth of technologically intensive, knowledge-based 
businesses, and of facilitating links between academia and the business 
community. KOM will perform these roles. By adopting such an approach, KOM will 
play a new role in the development of the Omani economy. Assuming all things 
equal, it'll help create new jobs, attract foreign capital and increase local 
and national competitiveness. <br>
The Park's incubator programme (The Knowledge Mine) - which is intended to link 
talent, technology, capital and professional know-how - will provide affordable 
space and business facilities, thus reducing the costs of start-ups. It'll 
provide advisory, information services, management and marketing support, 
linkages to research facilities and access to capital, thereby enhancing the 
chances of success of the early-stage technopreneur. <br>
KOM's relationship with Omantel should also be highlighted. Having the country's 
telecom provider as a partner that shares our vision, adds tremendous value to 
the range of services that KOM can offer its tenants. <br>
As far as KOM's location is concerned, it's ideally placed - on the doorstep of 
SQU and the town of Seeb, the fast growing community in Muscat - for businesses 
starting up or expanding in the GCC region. Factors important to investors, 
employers and employees, I believe, can all be satisfied in the Sultanate. 
Oman's workforce is educated and multilingual, while transport and communication 
links are world-class. Add to this KOM's proactive business-oriented management 
team, keen to attract inward investment, and it's clear the opportunities are 
here for the taking. <br>
In my opinion, domestic and international businesses will be attracted to KOM 
not only by the points I've touched on, but also by the benefit of close links 
with the academic community and facilities of SQU. KOM will be a part of a 
virtuous cycle in which technology is generated and new knowledge is unfolded. 
In fact, KOM's impact won't be limited to its own tenant companies, but will 
reach other companies that are not physically located there, but that use many 
of its services and establish links with KOM tenants and institutions. It's this 
kind of business thinking that will make KOM highly attractive. <br>
<br>
<b>OER:</b> How is KOM expected to contribute 
towards building a Knowledge Economy culture in Oman? <br>
<b>HE Sunaidy:</b> This is an interesting question. KOM is coming on-stream as the 
Knowledge Economy gathers momentum. Education and training are crucial 
ingredients in developing a Knowledge Economy culture, and KOM already houses 
two leading private IT colleges. The first to open its doors was the Middle East 
College of Information Technology (MECIT), an affiliate of India's Manipal 
Academy of Higher Education, where more than 200 students have already enrolled. 
A further 186 students will be studying at the Waljat Colleges for Applied 
Sciences, an affiliate of the Birla Institute of Technology, and KOM is located 
just over a kilometre from SQU. <br>
E-related issues are also underway in government circles. For example, an IT 
Task Force from the Ministry of National Economy is working towards setting up 
e-government initiatives. <br>
From the private sector, many domestic businesses are already executing their 
day-to-day trading activities via OmanTradaNet's iX2 Exchange. The country's 
finance community is taking a lead in the region's e-banking revolution. There 
are other initiatives at play, such as the SANAD project, which involves Bahwan 
Cybertek. So, KOM is joining a domestic e-movement that's already beginning to 
pick-up speed. <br>
<br>
<b>OER:</b> What kind of a marketing platform is 
being created to promote KOM? <br>
<b>HE Sunaidy:</b> In order for KOM to generate real credibility, build perceptions, 
engender name recognition and break down barriers to trust, it'll have to 
utilise a host of marketing tools and tactics that clearly differentiate KOM and 
appeals to our target audience in new, creative and dynamic ways. These are the 
issues that will shortly be rolled out. <br>
<br>
<b>OER:</b> What type of companies do you expect 
to see in KOM? <br>
<b>HE Sunaidy:</b> I would expect to see a diverse set of companies in KOM. Examples of 
areas that tenants might be working in include: <br>
<b>Enterprise software and hardware: </b>Companies developing and supplying 
software and hardware products/platforms for use by enterprises. <br>
<b>Infrastructure:</b> Firms developing and supplying technologies/products that 
provide a basis for the development of applications and products and are not 
focused on a specific market vertical. <br>
<b>Wireless/electronics and industrial automation:</b> Companies developing and 
supplying electronic devices and systems for industrial application. 
Image-processing systems for control applications in industry. Integrated 
electronic and IT systems for civil and industrial applications of smart card 
contact-less technology. Companies supplying and developing applications for the 
wireless Internet or building infrastructure technology to support the 
proliferation of the wireless infrastructure.<br>
<b>e-Business and Internet applications: </b>Firms that are building and 
supplying applications that leverage the Internet (application service 
providers, call centres and e-business exchanges). <br>
<b>Environment:</b> Companies involved in developing and supplying environmental 
engineering techniques; environmental models; landscape ecology; environmental 
impact assessment; territorial and environmental planning. Research and 
development of new technologies for water treatment, waste recovery and 
valorisation, site assessment and environmental reclamation. Innovative 
technologies and systems applied to maritime transportation of crude oil and 
petroleum products. <br>
<b>Qualified Services: </b>Companies engaged in consultancy, training and 
education; e-training and virtual reality simulation techniques; ergonomics; 
event planners and organisers; international organisations; media; design; 
public relations; and healthcare. The healthcare sector is a particularly 
interesting field. For instance, the Medicom software was developed here at the 
Royal Hospital - software that's now selling globally. This indicates that we've 
already got high-tech, knowledge-driven businesses based in Oman, and I'm 
confident that KOM will contribute to their future development and progress. <br>
<br>
<b>OER:</b> Which companies have confirmed 
tenancy at KOM? <br>
<b>HE Sunaidy:</b> As discussed earlier, MECIT is already operating with 200 students 
while the Waljat Colleges for Applied Sciences will take up residency in April. 
Omantel is already a partner and is coming on board with Siemens. Gulf Air will 
set up a sizeable call centre. Omantel, Bahwan Cybertek and PEIE are also 
considering opening up a call centre at KOM. GAVS Information Services Ltd, a 
Chennai-based solutions firm with offices in the US and Europe has also 
confirmed its tenancy. It's early days yet, but the interest in KOM is most 
definitely there, from international venture capital firms to blue chip, 
multinational ICT companies. Suffice to say, the team is very encouraged by the 
response it has received so far.
</font></p>
