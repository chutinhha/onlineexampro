		six-speed manual transmission. “The new VQ-series V6 has had some major 
		reworking done, helping it provide better performance in terms of power, 
		emissions and fuel economy” commented Mustansir Lakdawala, Infiniti 
		Brand Manager at Infiniti Middle East. The G sedan’s engine now has 327 
		horsepower and 38.6kgm of torque.<br>
