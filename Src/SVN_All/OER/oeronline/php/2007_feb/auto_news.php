        <b><font face="Verdana" size="2" color="#CC3300">It’s all about safety<br>
        </font></b><font face="Verdana" size="1">
        <br>
		To Subaru, safety means protecting a car’s occupants in the event of a 
		collision and more. Ideally, a passenger vehicle should provide the 
		driver with the means to help avoid hazardous situations. Subaru calls 
		this concept Active Driving/Active Safety. This gives the driver a 
		strong sense of control, maneuverability and security. The Subaru 
		Symmetrical All-Wheel Drive forms the core of the Subaru Active 
		Driving/Active Safety concept. It gives a Subaru a significant traction 
		advantage over two-wheel drive cars (front- and/or rear-wheel drive) on 
		all road surfaces. On all 2007 Subaru models, the standard 4-channel / 
		4-sensor Anti-lock Brake System (ABS) incorporates Electronic 
		Brake-force Distribution (EBD).<br>
		<br>
		This system enhances braking performance and stability by actively 
		optimising braking force between the front and rear wheels according to 
		how the vehicle is loaded with passengers and cargo. Meanwhile, the 
		compact Subaru Boxer engine helps enhance safety, because it allows more 
		crush room in front and on the sides. Subaru vehicles are designed to 
		allow the entire power train to slide under the car along the floor 
		tunnel in a severe collision.<br>
