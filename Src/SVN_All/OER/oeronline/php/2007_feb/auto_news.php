        <b><font face="Verdana" size="2" color="#CC3300">Kue wows America<br>
        </font></b><font face="Verdana" size="1"><br>
		Kia Motors America (KMA) unveiled the all-new Kia Kue crossover concept 
		vehicle at the North American International Auto Show (NAIAS) in 
		Detroit. The Kia Kue employs a different approach to the crossover 
		utility vehicle segment by taking elements of what makes a CUV to a new 
		level, namely performance. Kue is designed first and foremost as a 
		driver’s vehicle. While possessing SUV characteristics and capabilities, 
		the Kia Kue focuses more on giving the driver an exhilarating driving 
		experience on the way to the home improvement centre than on how much it 
		can haul. The Kia Kue has a moncooque platform that sits on 22-inch 
		tyres with a wheelbase of more than 114 inches, packaged in an overall 
		length of 186 inches. Though purely a concept, the Kia Kue does, 
		however, hint at future styling directions for Kia Motors. “Good design 
		shouldn’t look like you are trying too hard,” said Tom Kearns, KMA chief 
		designer. “The future Kia direction will incorporate the ‘less is more’ 
		philosophy but still include a heavy dose of boldness to be distinct.”<br>
		<br>
		With its four wheels pushed to the corners and exterior skin tautly 
		stretched over its underpinnings, the Kue sits powerful and poised. 
		Draped muscular wheel flares play a nice contrast to crisp bevelled 
		sections found elsewhere on the body. Swept back headlights connect to 
		the front grille to create a clean, aggressive down-road appearance. The 
		side window shape flows effortlessly into the rear backlight where two 
		centreline, hinging doors open to access rear storage.<br>
