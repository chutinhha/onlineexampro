        <b><font face="Verdana" size="2" color="#CC3300">General Motors continues to grow<br>
        </font></b><font face="Verdana" size="1"><br>
		<img border="0" src="auto3.jpg" width="150" height="102" align="left" hspace="5" vspace="5">General Motors (GM) continued its record sales growth in Oman to 
		register annual sales of 2,320, up 43 per cent over 2005. This growth 
		has been fuelled by a succession of new car and SUV launches throughout 
		2006. A best ever month of sales in the Middle East in December – GM’s 
		38th consecutive month of record sales – took total annual sales to 
		140,509, an increase of 24 per cent over 2005. This means that GM’s 
		sales in the region have almost tripled in the last three years. “Our 
		continued success is driven by the growing popularity of all our brands, 
		but especially by Chevrolet,” commented Terry Johnsson, Managing 
		Director of GM Middle East. “We launched 17 new models or new 
		generations of existing models in 2006 and these have been very well 
		received by customers throughout the Middle East.” Chevrolet is the main 
		driver of the GM growth in Oman. In 2006 sales of Chevrolet in Oman were 
		up by 30 per cent to 1,456. At the same time, the premium brands, 
		Cadillac and HUMMER, saw sales grow by 166 per cent to 359, while GMC 
		recorded a 59 per cent increase to 502.<br>
