<p align="justify" dir="ltr"><font face="Verdana" size="1">
The adage goes: What is not defined cannot be managed; What is not managed 
cannot be measured; What is not measured cannot be improved.<br>
<br>
IT governance precisely helps organisations in defining, managing, measuring and 
improving IT investments and processes. All Omani organisations need to include 
IT governance as a part of their boardroom agenda and manage IT from a business 
perspective rather than a technology perspective.<br>
