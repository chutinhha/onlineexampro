<b>Ignore IT Governance At Your Own Peril</b></font><font face="Verdana" size="1"><br>
<br>
<b>D. Balasubramaniam demystifies IT governance and emphasises how it can lead to 
best business practices and growth<br>
</b><br>
“IT has been the longest running disappointment in business for the last 30 
years”<br>
<b>– Jack Welch, Management Guru and ex-Chairman of GE, addressing the World 
Economic Forum meeting in 1997. </b> <br>
<br>
Ten years on, the outlook towards IT has changed a lot. Corporate and public 
sector bodies across the world have realised the importance of IT as a 
transformational tool and business enabler. The Information Systems Audit and 
Control Association (ISACA) and its associate body, IT Governance Institute (ITGI), 
have contributed much to the development of the IT governance and in creating 
the COBIT framework – a toolset that helps organisations to increase the value 
returns on their IT investments.<br>
<br>
<b>IT Governance </b><br>
The IT Governance Institute (www.itgi.com) defines: “IT governance is the 
responsibility of the board of directors and the executive management of an 
organisation. It consists of the leadership, organisational structures and 
processes that ensure that the organisation’s IT sustains and extends the 
organisation’s strategies and objectives.” <br>
<br>
The process of implementing IT governance starts from the time of acknowledging 
IT as a strategic, critical and integral component of “enterprise governance 
framework”. Studies indicate that businesses with a formal IT governance 
functions in place have earned about 25 per cent higher profit margins than 
those without proper governance.<br>
<br>
Fundamentally, IT governance is concerned about two things –delivery of value to 
the business and mitigation of IT risks. The first is driven by the strategic 
alignment of IT with the business. The second is driven by embedding 
accountability into the enterprise. Both need to be supported by adequate 
resources and measured to ensure that results are obtained. This leads to the 
five main focus areas (domains) for IT governance:</font></p>
