<p align="justify" dir="ltr"><font face="Verdana" size="1">
<b>IT Governance in Oman</b><br>
<img border="0" src="spotlight2.jpg" width="276" height="269" align="right" hspace="5" vspace="5">With heightened economic activity in the region, IT investment in Oman has been 
on the rise. Some corporate and government entities in the Sultanate are 
investing in IT and managing IT from a strategic standpoint, but a majority of 
organisations continue to look at IT as a simple support function.<br>
<br>
In Oman, while realisation has dawned that IT governance is an integral part of 
corporate governance, there continues to be lack of clear understanding. 
Questions frequently asked are: What is the right IT requirement for an 
organisation? What is the right investment? How does it benefit the organisation? 
How do we measure benefits? How do we determine return on investment? There also 
a need for qualified professionals to be part of the IT governance process so 
that the right answers can be provided to the management. <br>
<br>
The Information Technology Authority (ITA) of Oman is investing in efforts and 
resources to evolve an IT governance framework and an information security 
governance framework as a subset within the former, specifically for government 
agencies and affiliate bodies. The primary objective of the ITA is to transform 
Oman into a knowledge-based economy and enable delivery of government services 
via electronic channels – normally referred to as e-governance. In the process 
of integrating the eservices of various government agencies, ITA also seeks to 
help in strengthening internal IT processes and thereby maturing IT governance 
within all participating government bodies.<br>
<br>
The Muscat Chapter of ISACA has also been playing an active role in promoting 
the cause of IT governance and the COBIT framework in Oman. The chapter conducts 
regular seminars and workshops to create awareness on all domains of IT 
governance. The response for these activities from local IT and business 
professionals has been highly positive.</font></p>
