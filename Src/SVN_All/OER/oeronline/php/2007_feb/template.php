</font><font face="Verdana"><b><font size="4">The luxury of work</font><font style="font-size: 13pt"><br>
</font></b></font><font face="Verdana" size="1"><b>
Desmond Hatton, General Manager of one of Oman’s largest resorts, 
Shangri-La’s Barr Al Jissah Resort &amp; Spa, tells Rekha Baala that his environment 
is one of the best places to de-stress<br>
</b><br>
<b>
<img border="0" src="pastime.jpg" width="175" height="163" align="left" hspace="5" vspace="5"></b>Taking time off is a luxury for Desmond Hatton, general manager of one of Oman’s 
largest resorts, Shangri-La’s Barr Al Jissah Resort &amp; Spa. So punishing is his 
daily schedule that he laughs when asked whether he puts in more than 10 hours a 
day. “That will probably be a half-day for me.” Desmond gets to his office at 7 
am and retires for the day at hours that keep changing.<br>
<br>
Desmond joined Shangri-La’s Barr Al Jissah Resort &amp; Spa long before its opening. 
He came to Oman after 14 years with the Shangri-La group, serving earlier in the 
Philippines, Malaysia and Fiji. Among the 27 years he has spent away from home, 
Oman has been is his tenth and latest ‘interesting’ stop. Being the general 
manager overseeing the running of three hotels has its own share of 
responsibilities. And, Desmond is constantly shuttling between Al Waha, Al 
Bandar and Al Husn meeting guests and supervising 1,100 employees. “Oman is a 
stunning country with unspoilt beauty.”<br>
<br>
<b>Managing time</b><br>
So, where does he get time for himself, away from the resort. All this, even as 
he has not ‘switched off’ from his work. “The hotel business is the wrong 
business if you want to switch off. Even if the property is relatively quiet, 
you tend to get busier as there other things to catch up on. I try not to take 
my work home with me but I do wake up during the night with various projects and 
ideas running through my head. You can either love or hate this business. And I 
simply love it!”<br>
<br>
But there are times, Desmond feels, when you have to take that time for 
yourself. This, he proposes to do by “seeing more of Oman and spending holidays 
away from hotels. This summer, I plan to stay in a villa in Bali and then spend 
a few weeks in my native Ireland.”<br>
<br>
<b>Making space<br>
</b>Walking his two dogs along the beach at Shatti Al Qurum, one can see Desmond 
is “totally relaxed and completely different”. Even though he admits he “has 
been lazy what with Christmas and New Year.”<br>
<br>
But Desmond does try and work out. It is important we exercise and generally 
take care of our health. It is easier to put on the kilos than take them off. 
Walking around the resort a lot helps in a way but still there are other sports.<br>
<br>
“I enjoy diving but haven’t enjoyed it yet in Oman. The dive centre at the 
resort is almost ready and hopefully I will be one of the first to use it. There 
are so many untapped areas in Oman and diving is going to be a beautiful 
adventure.”<br>
<br>
If there is the pleasure of diving to look forward to, there is something else 
which demands everyday attention. Stress is definitely an inherent part of 
Desmond’s high-profile job. “I do get stressed but try not to show it as it 
reflects on the staff. To get away from this, I normally go for a walk around 
the place till I am relaxed.”<br>
<br>
To others like him who want to beat stress, he also offers the perfect place to 
unwind. “I would suggest there is no better environment to beat stress than a 
weekend at Shangri-La’s Barr Al Jissah Resort &amp; Spa,” he says, chuckling at the 
perfect sales pitch.</font><p align="right" dir="ltr" style="margin-left: 10px; margin-right: 10px">
