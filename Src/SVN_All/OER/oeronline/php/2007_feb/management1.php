<p align="justify" style="margin: 3px 10px"><font face="Verdana" size="1"><br>
</font><font face="Verdana" size="4"><b>Flexitime? Think Again <br>
</b></font><font face="Verdana" size="1"><b>The new staffing policies could see American retail workers forced to be 
available on call</b><br>
&nbsp;</font><div align="center">
	<table border="0" cellpadding="0" style="border-collapse: collapse" width="95%">
		<tr>
			<td width="360"><font face="Verdana" size="1">Just when you thought that the American worker could not get squeezed for any 
more juice, major retailers have come up with a new cost-savings innovation to 
apply more pressure on their workforce. Indeed, staffing is the latest area 
where big retailers like Wal-Mart, Target, and Payless hope to wring out a few 
pennies with new operational efficiencies.<br>
<br>
Mind you, Wal-Mart and its kin already have been blasted for low wages, being 
miserly with health benefits, and reticent about pay overtime. So how could 
matters get worse for their employees?<br>
<br>
Major features in The Wall Street Journal and the San Francisco Chronicle reveal 
that new computerised scheduling systems will move many American workers from a 
predictable work shift to staffing based on the number of customers in a store 
at any given time. The system will allow managers to start the business day with 
a few employees on hand, then bring in “on call” workers when business picks up 
during the course of the day. Once commerce lags, the manager can send workers 
home until further notice. In short, it more closely matches staffing to 
customer demand.</font></td>
			<td valign="top">
			<p align="center">
			<img border="0" src="management2.jpg" width="123" height="172" vspace="5"><br>
			<font face="Verdana" size="1"><b>David Batstone<br>
			is the author of Saving<br>
			the Corporate Soul and<br>
			teaches business ethics<br>
			at University of San<br>
			Francisco, United States</b></font></td>
		</tr>
	</table>
</div>
<p align="justify" style="margin:5 10; "><font face="Verdana" size="1"><br>
It’s easy to see how this practice could increase productivity and, hence, 
operational profitability. But consider the plight of workers who no longer can 
budget their expenses for a week, or arrange for babysitting for their kids if 
called into work, or hold a second job that might help them make ends meet. They 
are essentially at the beck and call of their boss on a daily basis. Rather than 
work, say, four 8-hour shifts per week, a worker might work two hours one day, 
six another, and three on a subsequent day. The practice will force a large 
slice of American workers into low-paid, part-time jobs.<br>
<br>
“The whole point is workers were a fixed cost, now they’re a variable cost,” 
Kenneth Dalto, a management consultant told The Wall Street Journal. “Is it good 
for workers?” he added. “Probably not.”<br>
<br>
I don’t know why Dalto had to throw the word “probably” into his assessment. 
It’s clearly a nightmare scenario for workers who will be pressured to be 
available at the drop of a hat. It gives “flex time” a whole new meaning. It 
used to mean that workers could arrange their work to fit their personal 
demands. Now it means that workers will arrange their lives to meet the demands 
of commerce.<br>
<br>
<img border="0" src="management3.jpg" width="250" height="184" align="left" hspace="5" vspace="5">Wal-Mart now asks its hourly employees to fill out a schedule of their 
availability, and encourages them to include a weekend window “if at all 
possible.” This “personal availability form” states: “Limiting your personal 
availability may restrict the number of hours you are scheduled.” The obvious 
implication is that the less flexible you are, you may find yourself in the 
manager’s dog house and fall to the bottom of his call list.<br>
<br>
The San Francisco Chronicle reports that some long-time workers, who have 
reached higher pay scales, fear that managers are using the system to pressure 
them to quit their jobs. After working 16 years at a Wal-Mart in Hastings, 
Minnesota, Karen Nelson says managers told her she had to be open to working 
nights and weekends. When she refused, her manager told her, “[I can] get two 
people for what I pay [you].” Her hours were cut and restored only after she 
filed a complaint.</font></p>
<p align="justify" style="margin:5 10; "><font face="Verdana" size="1">
<br>
While retailers can defend the new staffing policies by hiding behind improved 
customer service – workers will be present when the customers most need them – 
the truth of the matter is that it places an unfair burden on workers. Just how 
far can we squeeze the worker? It looks like mammoth American retail chains are 
willing to test that proposition.</font></p>

