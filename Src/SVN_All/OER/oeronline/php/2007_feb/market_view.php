		<b>
        <font face="Verdana" size="4">MSM eyeing the 6k mark</font></b><font face="Verdana" size="1"><br>
<b>The industry sector has propelled the MSM Index to register double-digit 
growth. However, the question is when is MSM going to touch the magical figure 
of 6k!</b></font><font face="Verdana" size="2" color="#CC3300"><b><br>
</b></font><font face="Verdana" size="1"><br>
The MSM Index ended the review month (December 21–January21) at 5,956.46 points, 
registering a gain of 5.25 per cent. In terms of sectoral indices, it was the 
industry sector index that stole the limelight, reporting a monthly growth of 
10.43 per cent. It was followed by the banking and investment sector that 
reported a growth of 6.36 per cent while the services and insurance sector index 
showed lacklustre performance, moving up by only 5.23 per cent.<br>
<br>
The top five gainers during the month were Oman Flour Mills (up 35.44 per cent), 
Gulf Investment Services (27.15 per cent), Oman Cables Industry (26.97 per 
cent), Dhofar University (26.28 per cent) and National Aluminium Products (20.83 
per cent). The top five losers were Oman United Insurance (down 15.03 per cent), 
National Pharma Medicine (9.09 per cent), Majan College (9.07 per cent), Salalah 
Port Services (5.35 per cent) and Financial Services (down 2.82 per cent).</font><p align="center" dir="ltr" style="margin-left: 5; margin-right: 10">
<img border="0" src="marketveiw3.jpg" width="465" height="369"></p>
<p align="justify" dir="ltr" style="margin-left: 5; margin-right: 10">
<font face="Verdana" size="1"><b>Other GCC Markets</b><br>
Most GCC markets declined during the month. The major losers were Qatar, Saudi 
Arabia and Kuwait. On a year-to-date basis, Oman was the only market that 
registered higher gains of 6.52 per cent while Bahrain and Dubai had marginal 
gains of 0.37 per cent and 0.12 per cent, respectively. <br>
<br>
<b>Oman’s Economy in 2007</b><br>
Oman’s GDP has risen to US$30 billion from a mere US$270 million in 1970. This 
phenomenal rise has led to an increase in its per capita income from US$400 to 
US$12,000. The progress is attributed to sound government policies, encompassing 
all sectors of the economy. This year, its economy should show strong 
performance due to high oil prices, strong domestic demand arising from 
increased oil receipts and steady growth of non-oil sectors. <br>
<br>
This is even though expenditure is expected to increase, owing to major 
investment in the oil and gas sector as well as the tourism sector. Overall, the 
economic outlook remains positive, with firm international oil prices keeping 
export earnings and fiscal revenue high. Also, progress in the economic 
diversification programme, particularly gas-based industrial projects and 
tourism ventures, should support economic growth. Encouragingly, many of these 
industrial ventures are being funded by foreign investors–a positive sign that 
the reform programme is yielding results. </font></p>
