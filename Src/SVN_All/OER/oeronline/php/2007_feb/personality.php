</span></b><font face="Verdana"><b><font size="4">Practical 
Philosopher</font></b></font><font face="Verdana" size="1"><br>
<b>Jadawy Al Riyami, country manager, Motorola, says it is all about being 
honest and believing in oneself, and admits he is a bit philosophical at times | 
By Rekha Bala</b><br>
<br>
<img border="0" src="person.jpg" width="191" height="216" align="left" hspace="5" vspace="5">Hard work. Guts. Determination. Drive. Aggressiveness. Today’s upwardly mobile 
professional needs all these and more to become a successful leader. But what do 
you get when you combine these much-needed qualities with a slightly 
philosophical bent? You acquire the ability to look within challenges and 
problems and come out you’re your own solutions. You learn to get by 12-hour 
workdays and manage stress. You learn to strike that elusive balance between 
work and your personal life.<br>
<br>
Jadawy Al Riyami, country manager, Motorola, admits he is a bit philosophical at 
times. “It helps. This frame of mind comes with experience.” He highlights this 
point by his own take on stress, interesting and worth pondering. “Stress 
management is like popping a tablet for your headache. The tablet may give you 
temporary relief. But you need to find a cure and for that you have to get to 
the root of it, find out whether it is related to pressure or a sinus problem. 
So, the most important thing is “one’s self management where you have to learn 
from your own experience and mistakes.”<br>
<br>
For Jadawy, “one’s self management” is an integral part. Armed with an 
engineering degree from the UK, he returned to Oman to work for sometime only to 
take off to Dubai for a somewhat complete shift in job. “Those where the days 
when no one went to Dubai. But I was not getting enough experience here and so I 
joined Emirates in the procurement department of all places.” The stint was not 
altogether fruitless and at Emirates, Jadawy picked up some important ”project 
management skills”. These are skills that Jadawy feels are important even today. 
“If you are already an engineer and working at a place where there is no scope 
for management, then you cannot be a manager. You have to work in management to 
acquire the skills.”<br>
<br>
<b>Work satisfaction is prime</b><br>
Jadawy’s career objectives have remained steadfast, whatever the job. “I made up 
my mind a long time ago that I always wanted to do something interesting. And I 
think the guiding factor in all my decisions has been self-satisfaction. I 
understand that I have to live with my own decisions.”<br>
<br>
Jadawy moved to Motorola seven years ago and as it’s country manager is today 
responsible for the Fortune 500 company’s operations in Oman. “As country 
manager, I am responsible to bring our vision of seamless mobility to life, by 
helping you connect simply and seamlessly to the people, information, and 
entertainment that you want and need. I am the guardian of Motorola ethics and 
corporate governance, and my job is quite intensive.” His growth from project 
manager to country manager has entailed a leap ‘into an ever-changing and 
challenging telecom industry’.<br>
<br>
As country manager, Jadawy’s biggest and toughest project was the GSM expansion 
project for Omantel in 2004. “It was a truly logistical challenge.” We had to 
work in tough conditions on mountaintops, from helicopters and we learnt a lot 
from the project. The practical experience has been invaluable. Achievements 
ride on being able to constantly deliver and earn respect in the working 
environment, especially when there is a lot of competition,” he expands.<br>
<br>
And how does he face competition? “I work harder and strive to be honest in all 
my dealings. This is my mantra in life.” And being straightforward does not 
necessarily mean being rude, Jadawy is quick to point out. He is in control of 
the situation thanks to his pragmatic yet, philosophical approach.<br>
<br>
<b>Balancing priorities</b><br>
Being country manager of a multinational’s operations in Oman is not easy, what 
with extended working hours and stress being a constant. So how does the young 
manager handle stress?<br>
<br>
“I try and follow tips from other people. But I think managing stress has to 
come from within. You need to be focussed on the total picture as that helps 
elevate you from the stress of the situation. Change within change. That is the 
key to stress management.” Jadawy unwinds by praying, exercising and swimming. 
“We live by the sea and it is a shame if we don’t make use of it.”<br>
<br>
Most of his spare time is spent at home, which is the best place to unwind with 
the companionship that family life brings. “I also try to be a sociable person,” 
he declares. “I attend a lot of family functions and weddings. In fact, one of 
my goals is to try and do more of this.”<br>
<br>
This essentially brings us to the all-important work-life balance, which Jadawy 
hugely endorses. “This balance is important because if you are relaxed you know 
how to deal with different situations that often crop up in one’s work routine. 
If you are tired and frustrated, you tend to constantly live on the edge, which 
is not a very good thing for your life or your career.” Ask him about his career 
goals and ambitions, and some of the qualities he mentioned earlier resurface. 
Without doubt, it is all about “being honest and believing in yourself.”</font></p>

