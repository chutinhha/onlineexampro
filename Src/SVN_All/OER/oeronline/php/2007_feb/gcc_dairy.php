        <b>Burj Dubai now 100 storeys high</b><br>
        Standing at a height of 347.3 metres, Burj Dubai is set to become the 
        tallest building in the world. It joins the league of six super-tall 
        buildings that are 100 or more storeys high in the world, according to 
        Emaar Properties.<br>
        The other six buildings are – Sears Tower in Chicago (110 floors), 
        Ryugyong Hotel in North Korea (105), Empire State Building, New York 
        (102), Taipei 101 (101) and John Hancock Centre, Chicago (100). <br>
        <br>
        Emaar Properties Chairman Mohamed Ali Alabbar; the company’s Board of 
        Directors and the designer of Burj Dubai, Mr Adrian Smith visited the 
        100th floor recently. Said Alabbar: “As one of only six super-tall 
        structures in the world with 100 storeys or more, Burj Dubai is already 
        an international landmark.”<br>
        <br>
        There are only 38 structures in the world that are 300 metres or more 
        high. It is now higher than three other tall structures in the region – 
        the Burj Al Arab (321 metres), Emirates Towers Hotel (309 metres) and 
        Kingdom Centre, Saudi Arabia (302 metres). Currently, only Emirates 
        Office Tower (355 metres) is taller than Burj Dubai in the Middle East 
        region.</font></p>
