		</font><font face="Verdana" size="2">Economic growth to slow down<br>
		</font></b><font face="Verdana" size="1"><br>
        The UAE economy will grow by 6.2 per cent in 2007, down from the 
        estimated 9.3 per cent growth experienced last year, according to the 
        ‘Gulf Economic Forecast’ from HSBC Bank Middle East. The bank further 
        predicted that the UAE’s annual economic growth rate would slow down 
        slightly further in 2008, to an estimated 5.8 per cent.<br>
        However, the outlook remains bullish for the UAE economy for the coming 
        two years. The HSBC report forecasts the overall real growth will remain 
        high with the increase in the country’s real non-oil GDP to be even more 
        marked.<br>
        <br>
        Against this backdrop of broad-based growth, while predicting that the 
        country’s annual inflation rate will be seven per cent this year and 
        will drop further to 6.5 per cent in 2008, the bank also sees the 
        possibility of higher inflation as a real risk, especially in Dubai.<br>
        <br>
        Simon Williams, HSBC’s Middle East Economist, said: ‘The UAE is now 
        firmly established as an economic force to be reckoned with – not just 
        in the Gulf region but internationally. While the country’s overall rate 
        of economic growth will slow slightly in the next two years as oil 
        prices ease and production falls, the non-oil sector will continue to 
        expand strongly. The outlook for the UAE, and for UAE companies remains 
        good.”<br>
        <br>
        According to Steve Brice, Regional Head of Research, Standard Chartered 
        Bank, Dubai, inflation is expected to be moderate for most countries in 
        the region during 2007 and most obviously in case of the UAE.<br>
        <br>
        “We estimate that inflation in the UAE will plummet to 7.3 per cent in 
        2007 from 13.8 per cent in 2006. This is based on an expectation that 
        rent increases in Abu Dhabi will decelerate dramatically after the 
        Emirate imposed a 7 per cent cap on rent increases this year. A similar 
        decision in Dubai has not changed our forecast, although it does reduce 
        the uncertainty surrounding it,” says Brice in his report. In a 
        country-by-country analysis, HSBC report points out that the UAE offers 
        the most attractive balance for investors.<br>
