        <b>Dubai gold trade up 37% </b><br>
        <img border="0" src="gcc4.jpg" width="150" height="136" align="right" hspace="5" vspace="5">Gold trade through Dubai reached US$14.75 billion in 2006, up 37 per 
        cent from 2005 when it was estimated at US$10.73 billion, according to 
        the Dubai Multi Commodities Centre (DMCC). During 2004, gold trade 
        touched US$10.17 billion.<br>
        <br>
        A total of 489 tonnes of gold was imported into Dubai in 2006, while 
        exports increased by 23 per cent to 274 tonnes. In line with global 
        trends, there was a minor decrease in imports in 2006, which touched 522 
        tonnes in 2005, according to DMCC.<br>
        <br>
        Says Ahmed Bin Sulayem, CEO of DMCC: “The figures compare very well 
        against other global gold centres, particularly in Europe, where higher 
        gold prices resulted in significantly lower demand in 2006.”<br>
        <br>
        According to Colin Griffith, Executive Director for Gold at DMCC, “Gold 
        trade through Dubai remained remarkably steady throughout the year, 
        touching US$3.58 billion, US$4.06 billion, US$3.37 billion and US$3.74 
        billion, respectively, for the four quarters of 2006.”<br>
        <br>
        “Gold prices, which averaged US$604 per ounce in 2006, have rallied in 
        the last few years, backed by strong investor sentiment,” says Griffith 
        adding “Several factors are expected to impact the prices of gold this 
        year, including fluctuations in the US economy, the price of oil, and 
        social and political unrest around the globe. We expect the market to 
        remain extremely volatile as a result.”</font></p>
