        <img border="0" src="gcc3.jpg" width="150" height="115" align="left" hspace="5" vspace="5">Saudi Arabia’s budget for fiscal 2007 is the largest of its kind in the 
        Kingdom’s history. The budget reflects the determination of the 
        authorities to generate maximum growth in the gross domestic product to 
        help tackle outstanding economic challenges. <br>
        <br>
        Total revenue is put at a hefty US$107 billion against spending of 
        US$106 billion, thereby projecting a $6 billion surplus. However, real 
        surplus would most likely be higher as the authorities have assumed the 
        average price of crude at US$35 per barrel. This is a rather 
        conservative figure and considerably below the average market rate. Oil 
        accounts for three-fourths of treasury income in Saudi Arabia. <br>
        <br>
        The increased spending should help Saudi officials solve some 
        outstanding challenges, notably of unemployment. The unemployment rate 
        stands anywhere between 8 to 13 per cent, depending on the source. 
        Undoubtedly, the problem is doubly dangerous as extremist groups tend to 
        find new recruits amongst the unemployed. Still, statistics are not 
        supportive, as one-third of nationals are below 15 years of age, thus 
        expected to enter the job market only in the next few years.<br>
        <br>
        The government has also allocated a robust US$26 billion for educational 
        services and another US$10 billion on health and other developmental 
        projects, all of which should help Saudi Arabia to improve its standing 
        on the human development index (HDI), annually published by United 
        Nations Development Programme (UNDP). The Kingdom occupied the 76th 
        position on HDI in 2006, the worst result within the Gulf Cooperation 
        Council (GCC) countries. <br>
        <br>
        Private sector firms tend to take a lead from public spending. Projected 
        expenditures for fiscal year 2007 is some US$12 billion above budgeted 
        figure for 2006. Accordingly, stronger public sector commitment in 
        spending tends to be matched by firm investment commitment from private 
        sector companies. <br>
        <br>
        But, projections aside, what matters at the end of the day are actual 
        spending figures.</font></p>
