		<p align="justify"><font face="Verdana" style="font-size: 14pt"><br>
        </font><font face="Verdana"><b><font size="4">The Economy 
        of Holy Places</font><font style="font-size: 14pt"><br>
        </font></b></font><font face="Verdana" size="1"><b>Both Haj and Umrah 
        contribute substantially to Saudi economy, writes Dr Jasim Husain Ali, 
        Economist and Member of Parliament, Bahrain</b></font></p>
		<p align="center">
		<img border="0" src="gcc.jpg" width="400" height="212"></p>
        <p align="justify"><font face="Verdana" size="1">The monetary value of 
        diverse activities involving the annual Haj plus Umrah (little Haj) is 
        substantial. Having been to Makkah and Madinah to perform Haj this year, 
        I had the opportunity to get a first-hand account of numerous business 
        activities that take place during the annual Haj. I also managed to 
        compile some statistics related to the business side of these holy 
        shrines. <br>
        <br>
        Understandably, statistics for 2006 are yet to be released, which 
        incidentally witnessed two Haj seasons, at the start and towards the 
        year-end. Accordingly, the monetary value of 2006 could be unusually 
        inflated; thereby caution must be exercised when interpreting the 
        figures. The Chamber of Commerce and Industry (BCCI) has reported at 
        least 10 per cent increase in Haj-related business activities this 
        season. The BCCI reported that majority of expenses covered 
        accommodation and food expenses.<br>
        <br>
        <b>Substantial value</b><br>
        According to a field study conducted by a group of Saudi economists, the 
        value of all activities related to the holy shrines amounted to US$31 
        billion in 2005. Undoubtedly, the figure is substantial – representing 
        about 12 per cent of Saudi Arabia’s gross domestic product (GDP) in the 
        same year.<br>
&nbsp;<br>
        Thus, ‘the economy of holy places’, if I may say so, is exceptionally 
        significant. What is more, this economic activity is sustainable as 
        millions of Muslims tend to grab the first available opportunity to go 
        Makkah and Madinah. A good number of nationals from the six-nation Gulf 
        Cooperation Council (GCC) perform Haj on a regular basis, taking 
        advantage of the available conveniences. Thanks to the arrival of new 
        regional airliners, GCC travellers to the holy places have plenty of 
        options to choose from. And for good reasons, Saudi authorities recently 
        upgraded Jeddah’s King Abdul Aziz Airport to an international one. 
        Accordingly, Prince Mohammed bin Abdulaziz Airport in Madinah had to be 
        upgraded to make it fit for receiving international flights on a regular 
        basis. <br>
        <br>
        The hefty figure of US$31 billion covers spending by visitors to the 
        holy sites throughout the year and not just the annual Haj. This 
        includes Umrah that can be carried out at any time during the year, but 
        particularly famous during the fasting month of Ramadan. This is notably 
        true during the last ten days of Ramadan, when hundreds of thousands of 
        faithful go to Makkah asking Allah for forgiveness and blessing. The 
        holy Quran was descended during the last ten days of Ramadan. Owners of 
        retail shops close to the Grand Mosque consider the last ten days of 
        Ramadan the best time of the year with respect to revenues. The period 
        coincides with the eid or feasting, as parents tend to buy new clothes 
        and give other gifts to their children and loved ones. <br>
        <br>
        <b>Everything sells</b><br>
        Business owners contend that almost any product can be sold in the two 
        holy cities, especially during the Haj and Ramadan. Indeed, little if 
        any marketing is needed to generate business, as pilgrims like to buy 
        anything and everything. This partly reflects the fact that Islamic 
        teachings encourage Muslims to spend money while visiting Makkah and 
        Madinah as a sort of reward to those who live and work in the two holy 
        cities.<br>
        Visitors from abroad, rather than local pilgrims, are credited for being 
        the most profitable source of business here. Not surprisingly, 
        authorities are paying increased attention to the needs of pilgrims 
        coming from all over the world. <br>
        <br>
        Conversely, Saudi authorities place restrictions on nationals during the 
        Haj in order to make way for foreign nationals, who in turn are more 
        willing to spend. Many Saudi nationals use their own vehicles rather 
        than rent taxies. Some Saudi pilgrims tend to sleep in their own cars, 
        thus denying business opportunities for property owners. <br>
        <br>
        <b>More Facilities</b><br>
        A breakdown of statistics shows that 68 per cent of spending by pilgrims 
        relates to accommodation and living expenses. This partly explains the 
        rising number of hotels and restaurants opening in the holy places. The 
        next biggest spending covers the cost of animals offered as sacrifices, 
        amounting to about 9 per cent of total expenditures.<br>
        <br>
        The average cost of sacrificed animals, including butchering and 
        processing, amounts to about $125 per pilgrim. Other expenditures deal 
        with purchases of gifts and spending on telecommunications, including 
        the internet. Prepaid cards are widely available during the Haj season – 
        to such an extent that foreign workers (albeit covertly) offer these 
        cards for sale to visitors even while working at points of entry. <br>
        <br>
        Private sector firms try to get the maximum benefit from visitors to 
        Makkah and Madinah by offering pilgrims all sorts of conveniences. 
        American hotels and fast food restaurants are common in the vicinity of 
        the Grand Mosque. Coffee lovers need not worry, as Starbucks outlets can 
        be found in the holy places. <br>
        <br>
        The holy shrine in Madinah is encircled by an extraordinary array of 
        fine hotels and luxury furnished apartments, thereby allowing most 
        visitors to stay within the vicinity of Prophet Mohammad’s Mosque. In 
        fact, most if not all, visitors walk to the holy shrine. Renowned 
        international names such as InterContinental and Hilton hotels are 
        located near Prophet Mohammad’s Mosque. In fact, InterContinental is 
        noted for having several outlets in Madinah, assuming different 
        religious names (Dar Al Hijra and Dar Al Iman). <br>
        <br>
        <b>Gateway to Makkah </b><br>
        In addition to Makkah and Madinah, other cities in Saudi Arabia, notably 
        Jeddah and Taif, benefit from pilgrims. Of all, the biggest beneficiary 
        is Jeddah. This partly reflects the fact that King Abdul Aziz Airport in 
        Jeddah serves as the gateway to Makkah and there is no commercial 
        airport in the holy city of Makkah itself.<br>
        <br>
        King Abdul Aziz Airport currently handles 14 million passengers 
        annually, which is double its natural capacity. The airport attracts 
        nearly two-thirds of the foreign visitors to the holy places. There is 
        yet another benefit that accrues from the fact that most pilgrims use 
        land transport from Jeddah to travel to Makkah. <br>
        <br>
        After completion of this year’s Haj, Saudi authorities revealed an 
        ambitious plan to increase throughputs at King Abdul Aziz Airport to a 
        maximum of 80 million per year over three phases. By the end of the 
        first phase in 2011, the airport would be equipped to handle 30 million 
        passengers per year. Expansion plans include allowing private sector 
        entrepreneurs to develop hotels close to the airport. <br>
        <br>
        Saudi tourism authorities also encourage wealthy pilgrims to visit other 
        places of the country, including tourist sites in the south, the 
        industrial places in Yanbu and Jubail as well as the petroleum town in 
        Dammam. Pilgrims also tend to visit the capital city of Riyadh to meet 
        officials. Clearly, Saudi authorities are increasingly looking at the 
        annual Haj as an opportunity to market the vast kingdom together with 
        its ever expanding business potential.<br>
        <br>
        <b>Employment opportunities</b><br>
        The annual Haj season provides employment opportunities for thousands of 
        Saudi nationals. The season alleviates the employment challenge facing 
        the Kingdom. Many Saudis engage in simple trading activities such as 
        selling foodstuff and prayer beads. Yet others use their private 
        vehicles to transport pilgrims to different points in Makkah, notably to 
        and from the Grand Mosque. It is not possible for Saudi officials to 
        track the size of extra income in the absence of a comprehensive tax 
        system. <br>
        <br>
        Saudi entrepreneurs see the Haj and Umrah as unique business 
        opportunities to promote their products. Fortunately, Saudi Arabia is 
        home to a large number of industrial undertakings, especially in the 
        food and drink industry. Certainly, the concept of economies of scale is 
        at work during the Haj season in Saudi Arabia, as pilgrims consume a 
        large amount of food and drink items during the event. Some pilgrims 
        tend to stay in Saudi Arabia for longer than normal periods. This is 
        particularly true of pilgrims from countries in the Far-East such as 
        Indonesia, many of whom pay once in a lifetime visit to the holy places. 
        In practice, this means more business.<br>
        Nevertheless, Saudi authorities must exert efforts to end some illegal 
        practices during the Haj, such as the sale of expired goods, in turn 
        blamed for food poisoning of some pilgrims. The other challenge relates 
        to sale of fake branded goods, with little if any restriction from 
        municipality officials.<br>
        <br>
        The annual Haj or pilgrimage is thus increasingly becoming an important 
        contributor to the Saudi economy. This is because visitors to the 
        shrines in Makkah and Madinah spend a substantial amount of money on 
        accommodation, food, transportation, gifts and telecommunications, among 
        other activities. In short, it is not only the pilgrims who benefit from 
        visiting the holy shrines as they seek forgiveness from God, but also 
        numerous sections of Saudi society, including businessmen and owners of 
        houses and cars, find opportunities to get satisfying commercial 
        returns.</font></p>
