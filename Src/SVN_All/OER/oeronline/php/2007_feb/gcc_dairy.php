		</font><font face="Verdana" size="2">No.1 free economy in the region</font><font face="Verdana" size="1"><br>
		</font></b><font face="Verdana" size="1"><br>
        <img border="0" src="gcc1.jpg" width="150" height="114" align="left" hspace="5" vspace="5">Notwithstanding its decline in ranking, Bahrain remains the most free 
        economy within the Gulf Cooperation Council (GCC) and the larger Arab 
        world. <br>
        <br>
        The authorities, though, will be worried at the fall in Bahrain’s 
        ranking. Bahrain came down 14 positions in the 2007 Index of Economic 
        Freedom, thereby ranked 39th in the world. In 2006, the country’s 
        ranking had come down by five positions. <br>
        <br>
        The Heritage Foundation and the Wall Street Journal publish the Index of 
        Economic Freedom report and both entities are noted for their 
        conservative ideologies and embracement of private sector initiatives. 
        They see government involvement in the economy as something negative. 
        According to the report, Bahrain’s economy is 68.4 per cent free 
        compared to 89.3 per cent in the case of Hong Kong, the most free 
        economy in the world.<br>
        The index uses the results of 10 variables to assign points to 
        countries. The variables are business freedom, trade policy, fiscal 
        burden, government intervention, monetary policy, foreign investment, 
        banking and finance, property rights, corruption, and labour freedom.
        <br>
        <br>
        Bahrain was awarded the best result (99.6 per cent) in fiscal freedom. 
        Undoubtedly, this reflected absence of tax on personal income. Also, 
        except for oil companies, commercial entities are not subjected to 
        corporate tax.<br>
        The next best performance relates to financial freedom, as Bahrain 
        serves the leading regional financial hub. Local and foreign individuals 
        and firms alike have access to market credit on market terms. Moreover, 
        foreign banks form a majority in the country (15 out of 25 commercial 
        banks in 2006). Bahrain achieved a score of 90 per cent in financial 
        freedom.<br>
        <br>
        Conversely, the worst performance (40 per cent) was recorded for labour 
        freedom. The report is not pleased with inflexible employment market, as 
        firms are required to hire Bahraini nationals. Also, firms are not 
        entirely free to dismiss workers.<br>
        <br>
        <b>Tackling poverty <br>
        <img border="0" src="gcc2.jpg" width="150" height="192" align="right" hspace="5" vspace="5"></b>Bahrain authorities are becoming increasingly serious in tackling 
        the poverty problem. The proof is that recently the government decided 
        to increase the amount of financial support extended to poor individuals 
        and households.<br>
        <br>
        A study conducted by the United Nations Development Programme (UNDP) 
        contends that a Bahraini family consisting of six members is considered 
        poor if its monthly income falls below US$936. Accordingly, some 11 per 
        cent of Bahraini families fall into a category below what the UNDP deem 
        poor. <br>
        <br>
        As suggested, there is good news with respect to tackling the poverty 
        issue. Only in January, the Ministry of Social Development announced 
        substantial increases in cash handouts. Based on a revised payment 
        schedule, the Ministry will pay US$187 instead of US$133 per month for a 
        single person, which translates into a 40 per cent rise. Also, a family 
        consisting of five persons will obtain a maximum of US$398 per month, 
        showing a whopping 50 per cent growth in payout. <br>
        <br>
        In 2005, the Ministry extended financial support to 10,295 families. 
        There are about 80,000 families in Bahrain, indicating that nearly 13 
        per cent of families received some sort of financial support from the 
        government. Some 20,960 Bahraini nationals obtained financial support 
        from the Ministry of Social Development in 2005. According to official 
        statistics, 4.7 per cent of total 448,491 nationals got some sort of 
        support from the government to help them meet ends.<br>
        <br>
        Additionally, many areas, especially villages, have their own locally 
        developed, independent, charitable entities that provide subsidies to 
        needy individuals and families. These charitable societies provide 
        support (either in cash or in kind) to more people, including some of 
        the same families receiving assistance from the government. <br>
		<br>
        It is believed that growing inflationary pressures have convinced the 
        authorities to increase financial support to needy people.<br>
