		<p align="justify"><font face="Verdana" size="1"><b>Dubai Holding restructures </b><br>
        A management restructuring that is expected to put in place a more 
        flexible executive infrastructure was announced by the government-owned 
        Dubai Holding. The decision aims at spurring and further supporting the 
        growth of its varied entities. In fact Dubai Holding has implemented 
        succession plans for Dubai Properties, Tatweer, Sama Dubai, Dubai Group, 
        Jumeirah Group and Dubai International Capital and TECOM Investments 
        with a Chairman and a Board of Directors for each entity.<br>
        <br>
        While the Chairmen and the Boards will be responsible for setting the 
        objectives and strategies for their respective entities, the Boards will 
        also maintain integrity through codes of ethics to safeguard the 
        credibility and reputation of their entities and that of Dubai Holding, 
        said Mohammad Al Gergawi, Executive Chairman of Dubai Holding.<br>
        <br>
        Some of the key appointments following the revamp include that of Hashim 
        Al Dabal, who has become the Chairman of the Board of Directors of Dubai 
        Properties with Mohamed Bin Brek as CEO.<br>
        <br>
        Saeed Al Muntafiq becomes Chairman of the Board of Directors of Tatweer 
        while Farhan Faraidooni is now Chairman of the Board of Directors of 
        Sama Dubai.<br>
        <br>
        Soud Ba’alawy has been promoted to the position of Chairman of the Board 
        of Directors for Dubai Group – a new entity that includes Dubai 
        Investment Group, Capita Investment Group and Noor Investment Group.<br>
        <br>
        Gerald Lawless is now Chairman of the Board of Directors for Jumeirah 
        Group while Sameer Al Ansari is Chairman of the Board of Directors of 
        Dubai International Capital.<br>
        <br>
        Some of Dubai Holding’s entities have acquired an impressive list of 
        global assets. These include Tunisia Telecom and Maltacom in which TECOM 
        and Dubai Investment Group became large stakeholders, Dubai 
        International Capital’s acquisition of the Doncasters Group and the 
        UK-based Travelodge Hotels Ltd, and Jumeirah Group’s hotel manageme nt 
        deals in Shanghai, London and Thailand besides acquiring Essex House 
        hotel in New York and plans for The Jumeirah Hotel on London’s South 
        Riverbank.<br>
