<p align="left" dir="ltr" style="margin-left: 5; margin-right: 5"><b><br>
<font face="Verdana" size="4">‘Invest in Export Development’</font></b></p>
<p align="justify" dir="ltr" style="margin-left: 5; margin-right: 5">
<font face="Verdana" size="1"><b>
<img border="0" src="coverstory9.jpg" width="191" height="223" align="left" hspace="5" vspace="5">Mohamed Ali A Amir Sultan, Director, W J Towell &amp; 
Co (LLC), talks about the group’s thrust on logistics development and strategy 
to promote Omani products in the international market. He also discloses what 
went wrong with Oman National Dairy Products (ONDP) in an interview with Akshay 
Bhatnagar<br>
<br>
What part of the business do you look after in the group?<br>
</b>WJ Towell Group is a leading private sector diversified group, engaged in 
manufacturing, trading, investments and other services. I primarily look after 
the group’s investments, Sultan products division, interests in international 
markets and trading side of the group’s business in the UAE. <b><br>
<br>
What is the focus of your investment strategy?<br>
</b>Our investment strategy has twin objectives – long term and short-term 
gains. We undertake investments, such as buying securities and private 
placements, among others. We keep a tab on market trends and developments, 
invest to ensure healthy returns. We have made major investments in companies 
such as Muscat Finance and United Power. In terms of sectoral investments, our 
thrust is on power, finance, real estate, food, and environment.<br>
<br>
We have ample opportunities for investments within the group itself. We are 
expanding our chain of restaurants, ‘Silk Route’, which offers Chinese and Thai 
cuisines by taking it to new locations like Sohar within Oman and Dubai in GCC 
region. We have joined hands with global giants, including Unilever and Nestle, 
to form two new entities–Towell Unilever and Nestle Oman Trading–to import and 
market their products in Oman. We also plan to further strengthen our foods’ 
business in which we are very strong.<br>
<br>
This apart, we are looking at real estate very closely as this sector is growing 
very fast in Oman. The Ministry of Tourism’s encouragement of big projects and 
the government’s permission to sell properties to foreign nationals in 
designated areas has fuelled growth in this sector. <b><br>
<br>
How are you expanding your distribution network?<br>
</b>Our subsidiaries, Mutrah Cold Stores and Cold Storage and Consumer Products, 
have an extensive distribution network across the Sultanate. We have formed a 
joint venture, Agility, with the logistics major, Public Warehousing Company (PWC). 
This company has constructed a 12,000 sq.m warehouse facility in Muscat apart 
from smaller, local storage facilities in Salalah, Sohar and Nizwa. The joint 
venture will take care of our logistics as well as delivery work in all 
locations, connected as it will be to the latest supply chain computer systems. 
So, by sitting in Muscat, we will be able to place orders in different locations 
in Oman, ensuring requisite deliveries.<b><br>
<br>
Regarding the Sultan products division, why is that most of your products are 
not manufactured in Oman?<br>
</b>Under the Sultan brand, we offer almost 26 product categories. The main 
items are cereals, fish products, coconut milk powder, etc. Most of them are 
currently being manufactured outside the Sultanate–for example, cereals are 
sourced from Germany and fish and coconuts are imported from Thailand. We have 
to source them from outside for multiple reasons. For most of our products, 
there is a lack of raw material in Oman. Secondly, it is because of cost 
effectiveness. Take fish, for example, it is expensive to import and package 
fish in Oman due to high labour costs. Here, Thailand offers price 
competitiveness. Thirdly, we market our products not only in Oman but in all GCC 
countries and Jordan, areas we can cover more conveniently and cost effectively 
from Dubai.<br>
<br>
Today, we market edible oil produced in Dubai. While we want to source edible 
oil produced in Oman, unfortunately we have not received sufficient support from 
local manufacturers, as they have their own brands. But we do market mayonnaise 
and vinegar that is produced in Sohar. We were selling bottled water made in 
Barka, but this was stopped as our supplier faced problems due to rising costs 
of making pet bottles. We hope to revive this soon.<br>
<b><br>
In your view what steps should be taken to promote Omani products, both 
domestically and in international markets?<br>
</b>Domestically, the need of the hour is a good distribution infrastructure and 
system. Local companies today spend a fortune in distribution costs as they 
invest in warehouses and resultant management and delivery systems to ensure 
availability of their products. Oman is not a small country: distribution here 
is expensive and warehouses are necessary in all big cities. Many companies can 
not afford such investments and they lose out in the market despite having good 
products. If the government sets up good warehouse facilities, making them 
easily available to domestic companies, it will do wonders for Omani products. 
While this will benefit the government, it will also help popularise and promote 
Omani products in the real sense.<b><br>
<br>
</b>On the export front, private companies currently are not making sufficient 
investments. I have noticed that in international companies, their export 
department generally has at least 6-7 people, tracking new markets, 
understanding them, identifying business opportunities, making inroads, ensuring 
follow-ups, etc. In Oman, however, most companies just appoint an export 
manager, leaving the entire work to him. How can we expect one person do all 
this? That’s not enough. Unless and until we make investments now, how can we 
expect returns tomorrow?<b><br>
<br>
</b>Also, domestic companies don’t spend enough to explore export opportunities. 
That’s where the government can help. The government can nominate 
people/agencies in countries where potential for Omani products/services exists, 
ensuring good market intelligence to domestic companies. Such knowledge can be 
shared among all concerned to save costs for individual companies.<b><br>
<br>
Are you saying that the government is not doing enough?<br>
</b>Government efforts in this direction have definitely been commendable, as is 
evident from the tremendous growth we have seen in our exports of non-oil 
products in recent years. But there is always scope for improvement, especially 
as we are aligning more closely with the global markets.<br>
<b><br>
Besides warehouses, what are the other logistical issues that need to be 
addressed?<br>
</b>Again, the government has been active in developing the logistics 
infrastructure in the country to facilitate trading. But, from the point of view 
of the business community, speedier action is desirable. Take ports, for 
example. Just having a port is not sufficient. We need to attract more shipping 
companies to provide services from these ports. Take Jebel Ali port in Dubai, 
one can reach out to any country from there. Jebel Ali gives us tremendous 
advantages for exports. Currently, this is not possible in Oman.<b><br>
<br>
How do you view the Oman-US Free Trade Agreement? What impact will it have on 
your business?<br>
</b>As a corporate entity, we welcome the Oman-US FTA. On opportunities for us, 
we need to study the US market closely and identify opportunities to suit our 
strengths. A large number of Arabs live in the US. While I don’t see any threat 
to us from the FTA, we still need to do our work perfectly.<br>
<br>
Whenever a country becomes part of the WTO or gets into any major trading pact 
with other economic bloc or a country, that country’s economy prospers at a 
higher rate. As I said before, Oman needs to further develop its infrastructure 
to attract more foreign investment.<br>
<b><br>
Do you think Oman’s pullout from the GCC monetary union was a wise move?<br>
</b>The government and the Central Bank of Oman must have studied the pros and 
cons of such a decision before it took a decision. On a personal note, I think 
monetary unification would have been good for Oman’s economy. If the unification 
happens and Oman remains away, the Sultanate might lose out on certain business 
opportunities. Oman will then remain a single market, similar to the UK and 
Switzerland are doing in the European Union.<b><br>
<br>
Talking of investment, what went wrong with Oman National Dairy Products (ONDP)?<br>
</b>The problem occurred due to an inaccurate IT accounting system. 
Unfortunately, some people within the organisation misutilised the system.<b><br>
<br>
But it happened right under your nose?<br>
</b>Even the auditors, the professional experts, failed to detect it. Someone 
within the company acted smart and damaged the company.<b><br>
<br>
Has any action has been taken against any individual(s) responsible?<br>
</b>The company has taken appropriate legal action.<br>
<b><br>
What are the lessons for you from this entire episode so that such incidents are 
not repeated in the future?<br>
</b>We were there as the non-executive directors of the board. I think the 
responsibility of the board has to be made more flexible. The authorities need 
to look into this aspect. Plus, there should be very strict guidelines for 
auditors also. They should not just look at regular accounts and vouchers but 
also look into the IT accounting systems as well. This will ensure that no one 
could play with the accounts, as proper checks will be in place.</font></p>
