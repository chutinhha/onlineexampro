            <p align="center"><b>
            <font face="Verdana" color="#FF0000" size="6">Wealth </font></b><b>
            <font face="Verdana" color="#FF0000" size="6">of</font></b><b><font face="Verdana" style="font-size: 35pt" color="#FF0000"><br>
            </font><font face="Verdana" style="font-size: 28pt">&nbsp;Knowledge</font></b></p>
            <p align="justify"><font face="Verdana" size="1"><b>Shape up or ship 
            out. This seems to be the mantra of the new economy that the 
            developed world has already ushered in. And with the digital divide 
            between the “haves” and “have nots” (that was already there in any 
            case) getting increasingly wider and wider with the advent of the 
            knowledge economy, it’s very important for the developing world, 
            including the Middle East, to pull up its socks. <br>
            <br>
            </b>Mehre Alam 
            does a reality check into the issues involved For countries in the 
            vanguard of the world economy, the balance between knowledge and 
            resources has shifted so far towards the former that knowledge has 
            become perhaps the most important factor determining the standard of 
            living - more than land, than tools, than labour. Today's most 
            technologically advanced economies are truly knowledge-based.<br>
            <br>
            <font color="#FF0000">•</font> World Development Report, 1999<br>
            <br>
            </font><font face="Verdana" size="3"><b>T</b></font><font face="Verdana" size="1">he 
            above conclusion, amazing it may sound, but it does precisely 
            pinpoint the pre-eminence of knowledge, and as a corollary, 
            knowledge economy, in today’s global village. Countries may have all 
            the resources, but still trail, and stand to trail not by years or 
            decades, but ages, if they lag behind in gearing up for, what is now 
            called, the new economy. <br>
            <br>
            Take the example of the US. More than 60 per cent of the workers in 
            this country are knowledge workers (defined as “symbolic analysts”). 
            To make things more clear, knowledge workers are those who 
            manipulate symbols rather than machines. And, who are they? Fashion 
            designers. Bank workers. Policy analysts. Teachers. Pharmaceutical 
            researchers. Architects…the list goes on. They all are the new face 
            of the new economy. <br>
            <br>
            Similar is the story of the other, major, advanced economies. More 
            than 60 per cent of the workers in these countries too are knowledge 
            workers. And technology, knowledge and innovation are the key 
            factors. <br>
            <br>
            In fact, if we compare some of the most highly industrialized 
            countries of the world, based on the indices of knowledge creation 
            and knowledge sharing, Japan bags the first place, followed by the 
            US, Sweden, South Korea, Iceland, Norway, Switzerland, Denmark, 
            Germany and Australia. <br>
            <br>
            <img border="0" src="cs1.jpg" align="right" hspace="5" vspace="2" width="210" height="156">And among the developing countries, Russia, Israel and Singapore 
            lead the pack as far as ushering in the knowledge economy is 
            concerned (based on the knowledge creation index). <br>
            <br>
            Then, there are the startling success stories: countries that have 
            turned the balance of trade in their favour in no time, thanks to 
            their strong focus on knowledge economy. Until recently, India was 
            known, internationally, for its politics and culture rather than its 
            economics. However, powered by its phenomenal success in Information 
            Technology (IT), it is now considered, along with the manufacturing 
            giant, China, as the emerging economic superpower. India never was 
            short on resources, natural or human. But its tilt (read excellence) 
            in new economy resources has propelled India into the forefront of 
            Global economies.<br>
            <br>
            Bangalore, the capital of India’s southern state of Karnataka, has 
            ushered in a “silicon revolution”. Major IT companies have cut a 
            swath toward this center. <br>
            <br>
            China too wants to build its ICT (information, communication and 
            telecom) infrastructure and economy with domestic spending and 
            expertise. Argentina, Mexico, Germany, Venezula and Peru, along with 
            India, are among the countries that are trekking the similar path. 
            They are all focusing more on knowledge, than resources. And if the 
            push is not coming forth at the government level, then it’s at the 
            grassroots and corporate level.<br>
            <br>
            The buzzword, today, is knowledge economy. And the essence of this 
            new age is: shape up or ship out. Create a knowledge-based economy 
            or stand to miss the bus. Access to information, knowledge creation 
            and knowledge sharing are pivotal. Countries that fail to spend on 
            the infrastructure for new economy are likely to be left behind by 
            ages. And the time will run out sooner than expected. <br>
            <br>
            Says Dr. Dr. Salih Osman, Assistant Professor in Economics, Sultan 
            Qaboos University: “The future clearly belongs to countries that 
            have greater access to information and knowledge source. The most 
            important thing to usher in the knowledge economy is to focus on the 
            human capital. The human factor is one of the most important factors 
            in a knowledge economy.” <br>
            <br>
            <b>The Definition</b><br>
            But, what exactly is the knowledge economy? According to a 1998 
            report of the United Kingdom Department of Trade and Industry, “A 
            knowledge-driven economy is one in which the generation and 
            exploitation of knowledge play the predominant part in the creation 
            of wealth.” <br>
            <br>
            There was a great turmoil in the West when machines began replacing 
            human labour, following the industrial revolution. In the industrial 
            era, it was the machines that were creating wealth. Over three 
            hundred years later, it is a different kind of change-induced 
            turmoil. The machines are being outpaced now. It’s the era that 
            people associate with high-technology industries such as 
            telecommunications and financial services.<br>
            <br>
            If one takes the most recent trends of literature in the fields of 
            management, technology, science research and economics, they all put 
            stress on the role of knowledge creation and sharing in the creation 
            of economic value at both the micro and macroeconomic levels. <br>
            <br>
            The domain of knowledge economy basically entails making effective 
            use of knowledge for development. And this requires developing 
            appropriate policies, institutions, investments, and coordination 
            across four key, functional areas (see box below).<br>
            <br>
            What, in fact, distinguishes knowledge from capital and labour is 
            that it strives to be a public good. So once it is discovered and 
            made public there is no additional cost to sharing it with more 
            users. Besides, the creator of knowledge finds it hard to prevent 
            others from using it. Devices such as trade secrets protection and 
            patents, copyright and trademarks provide the creator with some 
            protection.<br>
            <br>
            <b>Economic Freedom</b><br>
            Among the crucial factors that tend to give fillip to or curtail the 
            creation of a new economy is the presence of, or lack of, a degree 
            of economic freedom. Fred McMahon, the Director of the Center for 
            Globalization Studies at the Fraser Institute, Canada, sees a 
            definite relationship between economic freedom and knowledge 
            economy. “Economic freedom is crucial to the knowledge economy,” he 
            says. “It gives entrepreneurs the opportunity to experiment and 
            think afresh. It gives them the opportunity to innovate, to succeed, 
            and even fail, since economic opportunity will provide new 
            opportunities and failure is just a learning process to new 
            opportunities. Moreover, economic freedom protects your property 
            rights, including intellectual property – so when you do take risks 
            and succeed you will reap the rewards.” <br>
            <br>
            It may be noted that the Fraser Institute, supported by the John 
            Templeton Foundation, has launched a new initiative to promote 
            economic freedom in the world.<br>
            <br>
            McMahon is not in favour of government planning vis-à-vis the 
            promotion of knowledge economy. Says he: “Government planning 
            suppresses new thinking and experimental and reduces the economic 
            space for true entrepreneurs.” <br>
            <br>
            However, Dr. Osman sees the role of the government as the paramount 
            one. “The government holds the primary responsibility towards 
            building the infrastructure for the knowledge economy,” he 
            emphasizes. <br>
            <br>
            <img border="0" src="cs7.jpg" align="left" hspace="5" vspace="2" width="118" height="141"><b>The Great Divide</b><br>
            If some experts are to be believed, the world may be sitting on a 
            powder keg of digital divide. The ability to create, sustain, and 
            push the knowledge-driven economy now defines the parameters of 
            development. So infrastructure or heavy industries may be fine, but 
            not enough.<br>
            <br>
            The ability to innovate is what separates the developed world from 
            the developing world now. And it’s here that the statistics are 
            revealing, and frightening as well. If we see the distribution of 
            knowledge-based economies across the world, it comes to hardly 20 
            per cent of the countries. The digital divide, therefore, is 
            startling to say the least.<br>
            <br>
            Agrees Mohamed bin Abdullah bin Hamed Alharthy, Chairman of the 
            Omani Economic Association: “As is the case with many developing 
            countries, we find the gap between us and the developed countries 
            widening day by day.”<br>
            <br>
            So what happens if the digital divide continues to grow? “If the 
            divide continues at the present rate, it might lead to a big 
            dichotomy. Digital divide will lead to civilizational divide,” 
            predicts Dr. Osman. It’s not without reason that some experts 
            foresee growing conflicts in a world characterized by a marked 
            digital divide: a great divide on the basis on the new economy. <br>
            <br>
            <b>The Arab World</b><br>
            Among the Arab countries group, Jordan, the United Arab Emirates (UAE), 
            Oman, Tunisia, Bahrain, and Egypt are the leaders in knowledge 
            creation (see the graphs), as per the studies carried out so far. </font></p>
