            <p align="justify"><font face="Verdana" size="1">On the other hand, the UAE, Kuwait, Lebanon and Jordan lead the Arab 
            countries group in knowledge sharing. This indicates the leading 
            role for those countries in the ICT infrastructure development and 
            access to knowledge from abroad via foreign direct investment and 
            global trade.<br>
            </font><font face="Verdana" size="1">
            <br>
            </font>
            <img border="0" src="cs4.jpg" align="right" hspace="5" vspace="2" width="250" height="236"><font face="Verdana" size="1">Compared 
            to the developed countries, the</font><font face="Verdana" size="1"> 
            Arab countries scor</font><font face="Verdana" size="1">e low in 
            terms of knowledge creation or knowledge sharing, the two vital 
            aspects of the new economy. However, when compared to the developing 
            countries, the Arab countries outperform the former in growth 
            scores. This can be attributed to the fact that 50 per cent of the 
            Arab countries are oil-producing economies. Hence their ability to 
            maintain a higher overall average growth rate compared to the 
            developing countries.<br>
            </font><font face="Verdana" size="1">
            <br>
            </font>
            <font face="Verdana" size="1">However, the Arab world is pressed with some ticklish problems and 
            economi</font><font face="Verdana" size="1">c growth is area of real concern. Lack of growth leads to 
            economic instability, unemployment, brain drain, capital drain, 
            lower incomes, lack of economic diversity, accumulated debts and so 
            on. On all these counts, the Arab world has reasons to get worried. 
            Investing on the infrastructure for knowledge economy is one sure 
            way of getting out of the muddle. <br>
            <br>
            But the awareness about the challenges facing them is much greater 
            in the Arab world now</font><font face="Verdana" size="1"> than it was earlier. Eleven Arab countries are 
            members of the World Trade Organisation; seven others are in the 
            process of joining. Then, there are the regional economic 
            associations like the Greater Arab Free Trade Area (launched in 
            January 1998 comprising 17 countries) and the Gulf Cooperation 
            Council (GCC), consisting of six countries. There were several trade 
            agreements, including partnerships with the European Union (Morocco, 
            Algeria, Tunisia, Egypt, Lebanon and Jordan), and free trade 
            agreements with the United States (Bahrain, Jordan and Morocco, and 
            recently, Oman). The eagerness to usher in the new economy is 
            clearly there. What is perhaps required is to put an action plan in 
            place, with some sense of real urgency. <br>
            <br>
            <b>
            <img border="0" src="cs8.jpg" align="left" hspace="5" vspace="2" width="118" height="141">Oman: Challenges Ahead</b><br>
            Oman is taking steps in the right direction. The Sultanate’s economy 
            has tremendous potential. Of late, there have been concerted efforts 
            at the diversification of the economy, in line with the government’s 
            2020 Vision, whereby the dependence on oil has to be cut down. <br>
            <br>
            More and more industrial zones are coming up in the Sultanate. In 
            the GCC, Oman occupies a prominent place. <br>
            <br>
            According to Dr. Osman of SQU, Oman seems to be at the third 
            position in the GCC region, after Jordan and the UAE, in terms of 
            knowledge creation index (amount of research and development, state 
            of higher education, particularly in natural science, engineering 
            and communication etc.). In terms of knowledge sharing, Oman seems 
            to be occupying the fourth position, according to Dr. Salih. <br>
            <br>
            But the knowledge zones are conspicuous by the absence. “Oman has to 
            clearly work more towards building the infrastructure for knowledge 
            economy,” opines he. <br>
            <br>
            The first step, says the SQU academic, should be towards the 
            absorption of knowledge or technology in terms of provision of good 
            education, research work etc. Then comes the transfer of technology, 
            or the sharing of knowledge. The efforts have begun in this 
            direction. One very promising enterprise in this direction is 
            Knowledge Oasis Muscat (KOM), the technology park located 30 km from 
            Muscat, a major leap towards boosting knowledge economy in the 
            Sultanate. Ten-minute drive from the Seeb International Airport, KOM 
            neighbours Rusayl Industrial Estate - the Sultanate's largest 
            industrial park, and Sultan Qaboos University. <br>
            <br>
            From M-commerce to international call centers, KOM houses a wide 
            range of industry niches. Dedicated to supporting 
            technology-oriented businesses, KOM covers a total of 68 hectares – 
            12,000 square metres of office accommodation is already occupied, 
            with a further 10,000 square metres of office space coming on line 
            in July 2006. “More such institutions like KOM should be set up in 
            Oman to boost knowledge economy,” points out Dr. Osman. <br>
            <br>
            There have also been some concerted efforts in the Sultanate to 
            raise awareness on the need to usher in the new economy. The Omani 
            Economic Association (OEA) organized a conference on the “New 
            Economy” for the first time in the Sultanate. The conference was 
            organised in association with the United Nations Economic and Social 
            Commission for West Asia (ESCWA), Sultan Qaboos University and Oman 
            Chamber of Commerce and Industry.<br>
            <br>
            A legal entity, the OEA is registered in the civil association 
            registry of the Ministry of Social Affairs. OEA aims at promoting 
            socio-economic development activities in Oman through encouraging 
            scientific research. <br>
            <br>
            The “New Economy” conference, organized in October this year, was 
            addressed by some leading scholars, who spelt out why the Arab world 
            was still lagging behind in the New Economy. <br>
            <br>
            Addressing the seminar at the Sultan Qaboos University in Muscat, 
            His Highness Sayyid Asa’ad bin Tariq Al said, His Majesty the 
            Sultan’s representative, said such conferences are of significance 
            in terms of knowing about the latest technological information used 
            in quality control and good performance, calling on the youth of 
            Oman to arm themselves with technology and knowledge.<br>
            <br>
            “The Omani economy is progressing in sound steps, especially after 
            giving more attention to the issue of finding regional and 
            international partners,” Sayyid Asa’ad said in a statement during 
            his patronage of the conference. <br>
            <br>
            The conference, in a landmark development, announced the 
            establishm<img border="0" src="cs5.jpg" align="right" hspace="5" vspace="2" width="200" height="179">ent of the Gulf Economic Association (GEA). The main 
            objectives of this new association are to promote professional 
            collaboration among different associations and individuals, who are 
            interested in the economic issues of the GCC region. The GEA also 
            aims at supporting human development and economic reforms. The 
            members of the economic associations in Oman, the UAE, Saudi Arabia, 
            Qatar, Bahrain and Kuwait signed the memorandum of the GEA resolving 
            that Kuwait will host its headquarters.<br>
            <br>
            Also, in a major fillip to promoting the New Economy, the OEA and 
            the Economic and Social Committee of West Asia (ESCWA) signed an 
            agreement of cooperation with the objective of supporting the 
            efforts towards the transformation to knowledge-based economy 
            through developing small and medium enterprises, facilitating and 
            freeing trade, strengthening the relationship between the private 
            sector and universities in the field of scientific and technological 
            development, research, and enhancing the skills of the private 
            sector with respect to training and technology. <br>
            <br>
            Shaikh Mohammed bin Abdullah Al Harthy, chairman of the OEA, signed 
            the agreement on behalf of the OEA and Dr. Mervet Tilawi, Executive 
            Secretary-General of ESCWA, signed on behalf of the ESCWA. <br>
            <br>
            Commenting on the digital divide, the OEA chairman said: “As is the 
            case with many developing countries, we find the gap between us and 
            the developing countries widening day by day.” Dwelling on the new 
            challenges, he went on to add: “There is a need to face bigger 
            challenges, as Oman’s economy depends on oil and is far from 
            shifting to the competitive, productive economy.” <br>
            <br>
            <b>UAE shows the way: Dubai Media City, Dubai Internet City, Dubai 
            Silicon Oasis</b><br>
            Dubai is an emirate-city where the pace of development is so rapid 
            that the landscape seems to change every few months. With Its 
            imposing skyscrapers and astonishing island projects, Dubai is 
            leapfrogging towards the new economy in a planned and determined 
            manner. The Media City, Internet City, and the Silicon Oasis, 
            carefully nurtured and promoted by the government are solid 
            investments in the infrastructure of a knowledge economy. <br>
            <br>
            Inaugurated in January 2001, Dubai Media City (DMC) is now an office 
            to over 4,000 knowledge workers, with more than 780 licensed 
            companies operating on its premises. Through its focus on speed, 
            innovation, customer service and building an employee-friendly 
            environment, DMC has already carved out a niche for itself as a 
            knowledge hub, and is all set to create a special lead in the region 
            as a global media hub for talent, ideas and creativity. <br>
            <br>
            DMC provides an environment that attracts each and every element of 
            the media value chain in the community and has developed programs 
            that could be leveraged by the media community to explore expand and 
            channel the development opportunities. A host of magazines and 
            publications, and the electronic media companies operate from DMC. 
            It is the place where every kind of media business, specifically - 
            broadcasting, publishing, advertising, public relations, research, 
            music, new media and production and post-production are encouraged 
            to thrive.<br>
            <br>
            Global giants such as CNN, Reuters, Sony Broadcast &amp; Professional, 
            McGraw Hill Publishing, Bertelsmann, and MBC, along with regional 
            companies and new start –ups are already operating from the DMC. The 
            interdependent media community also boasts a growing talent pool 
            that offers a range of creative skills. <br>
            <br>
            DMC’s free zone status is an added attraction as it implies that 
            workers can change jobs without inviting bans, thereby attracting 
            some of the best talents in the media industry. Currently, over 170 
            freelance media professionals are based in the DMC.<br>
            <br>
            With its vision of creating and marketing world class enabling 
            services for the media industry, DMC’s success in a short time is a 
            remarkable development in the Arab world. <br>
            <br>
            Like the DMC, Dubai Internet City (DIC) too is a major hub for the 
            promotion of the knowledge economy in the region. As many as 5,500 
            knowledge workers are working in DIC. A knowledge economy ecosystem 
            designed to support the business development of Information and 
            Communications Technology (ICT) companies, the DIC is the Middle 
            East’s biggest IT infrastructure, built inside a free trade zone, 
            and boasts one of the largest commercial Internet Protocol Telephony 
            system in the world. <br>
            <br>
            A strategic base for companies targeting emerging markets in a vast 
            region extending from the Middle East to the Indian subcontinent, 
            and Africa to the CIS countries, covering 2 billion people with GDP 
            $ 6.7 trillion, the DIC offers foreign companies 100 per cent 
            tax-free ownership, 100 per cent repatriation of capital and 
            profits, no currency restrictions, easy registration and licensing, 
            stringent cyber regulations, and protection of intellectual 
            property. <br>
            <br>
            No wonder then that the DIC, within a very short span of time, is 
            now home to global ICT giants such as Microsoft, Oracle, HP, IBM, 
            Compaq, Dell, Siemens, Canon, Logica, Sony Ericsson and Cisco, to 
            name just a few. <br>
            <br>
            The companies that are based in DIC have a wide range of focus, 
            ranging from software development, business services, web-based and 
            e-commerce operations to consultancy, education and training, sales 
            and marketing and back office operations. Companies looking to 
            provide cost effective business process outsourcing (BPO) services 
            such as call center operations have an ideal place to bank upon as 
            the DIC provides a scalable state-of-the-art technology platform.
            <br>
            <br>
            <img border="0" src="cs6.jpg" align="right" hspace="5" vspace="2" width="200" height="242">DIC has also developed programs that can be leveraged by the ICT 
            community to explore and expand 'Channel Development' opportunities.
            <br>
            <br>
            In fact, the mushrooming ICT cluster at DIC also offers a high class 
            of business interaction and networking opportunities that can be 
            utilized for superior problem solving and knowledge sharing by the 
            community.<br>
            <br>
            One of the most interesting projects that will play the role of 
            promoting the knowledge economy and bridging technology divide 
            between the East and the West, will be the Dubai Silicon Oasis, the 
            technological arm of Dubai and the brainchild of General Shaikh 
            Mohammed bin Rashid Al Maktoum, the Crown Prince of Dubai. 
            Interestingly, not many people know about this landmark project that 
            aims to transfer technology into the region, especially in the 
            design and manufacturing of semi-conductors and electronic circuit 
            chips. <br>
            <br>
            Says Salah Eldin Eltayeb, Executive Manager, Media &amp; Communications 
            &amp; International Relations, Dubai Airport Free Zone &amp; Dubai Silicon 
            Oasis: “It is evident that Dubai has taken some major strides 
            towards closing the digital divide between the West and the East 
            with an objective of promoting knowledge economy.”<br>
            <br>
            Talking about Dubai’s infrastructure, in place, for the new economy, 
            he says: “Dubai has managed to attract a large number of global IT 
            companies, including Microsoft, Sun Computer, HP, to name a few, 
            which would not have been possible without establishing an enormous 
            IT infrastructure and the support of the government.” Dubai Internet 
            City and Dubai Media City, according to him, have made great 
            contribution in promoting knowledge economy and these two projects 
            have “given the Dubai government the confidence to loudly speak 
            about its initiatives towards closing the digital gap.” </font></p>
