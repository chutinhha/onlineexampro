                <p style="margin-left: 10"><b>
                <font face="Verdana" size="1" color="#FFFFFF">‘Bridge the 
                digital divide’</font></b></td>
              </tr>
              <tr>
                <td width="100%" bgcolor="#F8E0E0" height="1490">
                <p align="justify" style="margin-left: 10; margin-right: 10">
                <font face="Verdana" size="1"><b>
                <img border="0" src="cs10.jpg" align="right" vspace="5" width="100" height="83"></b><br>
                <b>Mohamad Mrayati, Regional Advisor – Science &amp; Technology, United 
                Nations, Economic and Social Commission for Western Asia (ESCWA), 
                explains to Mehre Alam, the parameters of a sound knowledge 
                economy<br>
                <br>
                Where, in your opinion, does Oman stand with regard to 
                knowledge economy? Do you think more needs to be done to prop up the new economy 
                here?</b><br>
                If the statistics available with the Economic and Social 
                Commission for Western Asia (ESCWA) is any indication, all our 
                member countries need to make the extra effort to usher in the 
                new economy, or what we call, a knowledge-based economy. Oman, 
                relatively speaking, is progressing well. But at the same time, 
                the Sultanate needs to intensify its efforts to lay solid 
                foundations for the knowledge-based economy. <br>
                <br>
                The Omani economy has performed very well in the last three 
                decades. Oman has done fairly well in sectors like 
                infrastructure, education, health, and employment in the public 
                sector, but when it comes to knowledge economy, more needs to be 
                done. <br>
                <br>
                There are some very positive developments in the Sultanate, 
                like, for example, the Knowledge Oasis Muscat (KOM), and the 
                several industry zones. But I think that instead of establishing 
                more and more industrial zones, the Oman government should go 
                for setting up more and more knowledge zones. <br>
                <br>
                Since the 1980s, the developing countries, especially the fast 
                developing ones, have not limited themselves to establishing 
                industrial zones only. They are, in fact, establishing zones 
                that have universities and research centers besides the 
                companies. And these zones have all facilities like marketing 
                and services (banks etc.). In short, it’s a zone with life, 
                knowledge, money, education, conferences. The industrial zones 
                no longer focus on the production lines only. It’s time Oman 
                started promoting zones with focus on knowledge, technology and 
                innovation.<br>
                <br>
                <b>Do you see some benchmarks in the GCC region with regard to 
                knowledge economy?</b><br>
                If we take the indicators of imports classified according to the 
                types of technology (low, medium and high), the GCC import 
                market, I think, is biased towards low and medium technologies.
                <br>
                <br>
                If we take indicators such as exports of technology-based 
                products, the number of institutions based on innovation, the 
                expenditure on research and development, entrepreneurship 
                activities, the GCC countries are still lagging behind, compared 
                with the advanced countries.<br>
                <br>
                If we take the indicators of information and communication 
                technology like the Arabic content on the internet or on CDs, 
                the number of sites in Arabic on the internet, the number of 
                pages on the internet in Arabic, the number of computers per 
                1,000 inhabitants, or the percentage of people having access to 
                the internet etc., we see a very clear digital divide. </font>
                </p>
                <p align="justify" style="margin-left: 10; margin-right: 10">
                <font face="Verdana" size="1">If we take the communication 
                networks and the Internet, we have witnessed lately an enormous 
                development of the mobile networks, but people don’t access the 
                Internet effectively through their mobile phones. Therefore, 
                they don’t use it for production and for economic development, 
                and the fixed telephone networks are not developed as fast. <br>
                <br>
                The Internet is linked basically to fixed telephone lines and 
                not mobiles. Since the advancement in mobiles is coming about 
                much faster than the telephone lines, the Internet is not being 
                used as extensively as it should be. Consequently, information 
                or knowledge is not being circulated, produced, and used, as it 
                should be.<br>
                <br>
                <b>What are the implications of knowledge economy for the 
                corporates in a country like Oman?</b><br>
                Knowledge is basically private. It is largely in the private 
                sector domain. Technology, worldwide, is basically owned by the 
                private sector, namely the companies. Unfortunately, in Oman and 
                other countries of the ESCWA region, (the six Gulf countries are 
                part of ESCWA), the private sector companies are not known to be 
                spending much on knowledge diffusion and generation and 
                consequently, on innovation. They import production lines and 
                rarely spend on research and development (R&amp;D). <br>
                <br>
                However, they now seem to be realizing that added values or 
                profit lie in innovation. The private sector in Oman and other 
                ESCWA countries (13 in all) ought to intensify their efforts on 
                R&amp;D, and spend more money on it. <br>
                <br>
                The governments, on their part, should encourage these efforts 
                with incentives, and should foster institutes that support the 
                demand on research. <br>
                <br>
                <b>And, what are the implications for the individual?</b><br>
                As I said earlier, we are moving towards an era of 
                knowledge-based economy and towards an information society. The 
                individual is living in an information society. Therefore, he 
                should have access to information. And it’s important that he 
                has access to this information at low cost, in his own language, 
                in different fields, at his home, at his school, at his 
                company…everywhere!<br>
                <br>
                This is beginning to happen. We hope it will be happening faster 
                than it is now in this part of the world. It’s very important 
                for the individual. With the advent of the information society, 
                life is no longer what it used to be. Life has changed, so has 
                the workplace, and so have the bases of our development as 
                individuals. Knowledge and information have become extremely 
                important for everyone: for the mother, for the child, for the 
                youth, for the companies, for the whole society.<br>
                <br>
                <b>What is the next stage of knowledge economy? Where do you see 
                it in 5-10 years from now? </b><br>
                This is a very important question and a very serious one too! If 
                we leave it at the current stage, the divide in knowledge (and 
                thus the knowledge economy) between the developed and the 
                developing countries will get wider and wider. <br>
                <br>
                At the rate at which knowledge-based economies are shaping up 
                and at the current pace of globalization, we may see more 
                chauvinism of technology (technological nationalism), greater 
                technological protectionism by the developed countries, and 
                wider divide between the developed and the developing countries 
                vis-a-vis knowledge in all aspects.<br>
                <br>
                If we analyze the world of today with regard to the spread and 
                use of knowledge, we will find that more than 80 per cent of the 
                knowledge economies are concentrated in only 20 per cent of the 
                countries of the world. This is a serious knowledge divide. This 
                divide is getting wider. It’s time the developing world changed 
                its economic policies to catch up with the developed world. The 
                developed world, on its part, should change its excessive 
                protectionism of knowledge and should not ignore the developing 
                world when it comes to sharing knowledge.<br>
                <br>
                The World Trade Organization (WTO) has already set up a working 
                group (in Doha) on trade and transfer of technology. I think the 
                developed countries should take this issue seriously. It is not 
                taking it seriously. The developing countries have been pressing 
                on the need to change the previous agreements, with a view to 
                improving the transfer of technology. But the developed world is 
                still reluctant. This has dangerous portents for the future. <br>
                <br>
                <b>Do you think that the international bodies like the WTO 
                should be doing more to bridge this technological divide? </b>
                <br>
                Certainly! Within the WTO, the organization for intellectual 
                property rights (WIPO), and agencies like the UNCTAD, and the 
                World Bank, should stress for more even distribution of 
                knowledge and more sharing of knowledge with developing 
                countries. Otherwise, the divide will be dangerous for world 
                peace. It’s time to take steps to bridge this divide! <br>
