huge success of BankMuscat’s recent Global Depository Receipt (GDR) issue listed 
on the London Stock Exchange has turned everybody’s attention on the use of 
Depository Receipts as a means of raising capital. While BankMuscat’s was the 
first such issue in the whole GCC region, it is likely to become the forerunner 
of a series of such issues across the region. <br>
<br>
<b>What are Depository Receipts? </b><br>
Depository Receipts are securities which allow, typically, the shares (although 
they can represent debt capital or preference share capital) of a (usually) 
non-US and non-European company to be traded on a major financial exchange. 
Depository Receipts are traditionally denominated in the US dollars and, 
depending on their structure and listing, are sometimes called ‘American 
Depository Receipts’, ‘Global Registered Shares’, ‘Global Depositary Receipts’ 
or ‘New York Shares’. The basic elements are, however, the same.<br>
<br>
One of the most common forms of offering the GDR is typically getting listed on 
either the Luxembourg or London stock exchanges and usually involves a tranche 
being made available to the US investors, taking advantage of listing and 
compliance exemptions available under the US law.<br>
<br>
Depository Receipts are fully negotiable and are traded independently of the 
underlying shares which they represent.<br>
<br>
<b>Why use Depository Receipts?</b><br>
The main attraction of a Depository Receipt issue, for the issuer, is its 
ability to tap into capital that may be unavailable or more expensive to obtain 
in its home market. An additional benefit is the increased exposure to, and 
enhancement of the image of the issuer in the US and Europe. <br>
<br>
For investors, they represent an attractive window into investing in a company 
listed on an unfamiliar exchange, with the added benefit of being denominated in 
US dollars and being as liquid as the underlying shares. Depository Receipts are 
usually interchangeable, at the option of the holder, into the underlying shares 
which they represent. <br>
<br>
It is commonplace for Depository Receipts to be offered at an initial discount 
to the&nbsp; market price of the shares which they represent, enhancing their 
attractiveness as&nbsp; an investment.<br>
<br>
<b>Issuing mechanics</b><br>
For a new share issue, the process commences with the issuer increasing its 
share capital in accordance with the rules of its jurisdiction. The new shares 
are then listed on the home exchange and are allotted to a depository bank. <br>
<br>
The depository bank holds the shares on trust for the investors and issues GDRs 
representing the underlying shares to the investors. The GDRs are listed and 
traded through an exchange, typically Luxembourg or London.<br>
<br>
In some cases, the depository may – to facilitate its role – appoint a custodian 
in the jurisdiction of the issuer to hold the shares on its behalf. The 
custodian is responsible for the day to day administration of the underlying 
shareholding represented by the GDRs, including receiving notices of general 
meetings, arranging the forwarding of dividends and the transfers of the shares. 
It advises the depository, who in turn advises the investors.<br>
<br>
GDR holders enjoy all the rights of a shareholder. These are exercised through 
the depository and typically include the right to vote, receive dividends and 
share in the assets on a liquidation. <br>
<br>
The process of issuing Depository Receipts representing existing (as opposed to 
new) shares is as follows:<br>
• The investor will request, through his broker, the purchase of shares in an 
issuer of Depository Receipts. If existing Depository Receipts are not 
available, the issuing process commences.<br>
• The broker contacts a local broker, who acquires the shares in the local 
market.<br>
• The ordinary shares are deposited with a local custodian, who instructs the 
depository to issue <br>
• Depository Receipts that represent the shares received.<br>
• The depository issues the Depository Receipts and delivers them, usually 
through<br>
DTC/Clearstream/Euroclear (as applicable) book entry.<br>
<br>
<b>Trading, transfer and cancellation</b><br>
The GDRs are tradeable on an exchange, if listed, or over the counter. 
Settlement is effected in the same manner as for other listed securities, i.e. 
through DTC in the US or via Clearstream/Euroclear in Europe. The depository 
bank acts as the registrar of the GDRs, and records the transfer and details of 
the new ownership of the shares.<br>
<br>
Depository Receipt holders can sell their holding by either selling the 
Depository Receipts themselves, or by surrendering their Depository Receipts and 
selling, on the local exchange, the underlying shares represented by the 
Depository Receipts.<br>
<br>
<b>Omani factors</b><br>
There are a number of local factors which any Omani joint stock company wishing 
to issue shares linked to Depository Receipts must consider. First, if it has 
insufficient authorised capital, this will need to be increased, which will 
require the approval of its shareholders in an extraordinary general meeting.
<br>
<br>
Full details of the proposed issue and the reasons for it should be provided to 
the shareholders to help inform about their decision. <br>
<br>
Secondly, the consent of the Capital Market Authority (CMA) will be needed for 
the issue. Where the issuer is a bank, Central Bank of Oman’s approval must also 
be obtained. <br>
<br>
A thorough, due, and diligent exercise is carried out by the issuer in 
coordination with its advisers, to ensure the accuracy and completeness of the 
information contained in the prospectus to be provided to potential investors. 
This prospectus must be prepared to comply with the legal and regulatory 
requirements of the jurisdiction in which the Depository Receipts are to be 
listed.<br>
<br>
Typically, an aggressive marketing campaign is launched. This begins with the 
commissioning of reports by respected analysts on the issuer, which is released 
to potential investors, and proceeds with a series of road-shows and press 
conferences.<br>
<br>
Permission of the CMA will be required for selling the Depository Receipts in 
Oman, even where the issuer is Omani, as the Depository Receipts will likely be 
viewed as foreign securities for the purposes of the Capital Market Law.<br>
<br>
It is known that a number of large corporates and banks across the GCC are 
considering using Depository Receipts as a means of tapping new sources of 
capital. BankMuscat’s spectacular success (it was seven times oversubscribed, 
and this was in excess of the expectations of Bank Muscat and their financial 
advisers, Citigroup) makes it increasingly likely that many others will follow 
suit.<br>
