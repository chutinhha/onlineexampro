<p align="justify" dir="ltr"><font face="Verdana" size="1"><b>First of all, how 
this Authority was formed and what is its role within the Sultanate?</b><br>
The Authority was established by Royal Decree 78/2004 promulgated on August 1, 
2004 implementing a law for the regulation and privatization of the electricity 
and water sector. The full provisions of the law came into effect on May 1st 
2005.<br>
<br>
The law establishes the Authority and outlines its competencies and its 
statutory functions and responsibilities. The law also implemented a 
comprehensive restructuring of the electricity sector. As a result of the 
restructuring, the operational functions previously undertaken by the Ministry 
of Housing, Water and Electricity (MHWE) are now the responsibility of newly 
created companies that undertake their activities under licenses issued by the 
Authority. <br>
<br>
The Authority is managed by three Members appointed by the Council of Ministers. 
I am the full time Executive Director and am joined by two non-executive Members 
Dr Saleh Mohammed Al-Alwai and Amur Mubarak Al Kiyumi. <br>
<br>
The Authority is an independent entity and reports to the Council of Ministers 
each year through the presentation of an Annual Report. Our first Annual Report 
will be presented around March 2006, and will include a number of issues 
required by the law, such as audited accounts, an assessment of developments in 
the electricity sector in 2005 and an outline of the further changes we envisage 
in the sector. The Annual Report is really a comprehensive policy brief to the 
government on electricity-related decisions that might come their way. The 
government remains in control of the overall direction and approval of policy, 
but the implementation of such policies falls to this Authority.<br>
<br>
In terms of the scope of regulation, this includes financial, economic, 
technical, and public interest matters – in fact, all matters related to 
electricity activities. The Authority grants licenses and exemptions that 
include conditions with which the holder of the license or exemption is required 
to comply. Much of our day-to-day work is taken up with the monitoring and 
enforcement of compliance of licensees and exemption holders.<br>
<br>
The Authority liaises closely with the government, in particular the Ministry of 
National Economy, to consider what further changes are needed to drive the 
electricity sector forward within the new framework.<br>
<br>
<b>Can you give us some background about yourself?</b><br>
I have been in Oman for over four years. I trained as an economist with the 
Government Economic Service in the UK, it was while seconded to the Scottish 
Office that I developed an interest in electricity privatization and regulation.
<br>
<br>
I then moved to the Office of Electricity Regulation and was fortunate to play a 
role in the regulation of the private utilities, working with Professor Steven 
Littlechild, who designed the framework of regulation that is now being applied 
in Oman. Later, I was approached by Abu Dhabi to set up the Economic Section of 
the newly formed Regulation and Supervision Bureau. <br>
<br>
I came to Oman at a time when the Cabinet had approved the preparation of a new 
sector law and its policies were being developed. So my job was to assist the 
government and its advisors to finalize the law, the regulatory framework, the 
licenses, codes and so on, and once the law was passed to help establish the 
Authority.<br>
<br>
<b>What most people will be concerned about is whether these changes will affect 
the electricity prices?</b><br>
The law says that tariffs charged to final customers must be Permitted Tariffs, 
i.e. approved by the government. All licenses issued to suppliers require them 
to charge only approved tariffs, either a Permitted Tariff approved by the 
Council of Ministers or a cost-reflective tariff approved by the Authority. 
Customers can be assured that no private entity can raise tariffs to improve 
profitability as the sector law and Authority will not allow it to do so.<br>
<br>
<b>Will progress be hampered as your production becomes ultra-efficient?</b><br>
Our job is to make sure that electricity production, transmission, distribution 
and supply is as efficient as possible. There are clearly some consumption 
inefficiencies, but we now have a transparent framework that will allow the 
government to direct subsidy where it is most needed. The government will 
determine who should benefit from subsidy and by how much. We expect some 
customer groups to receive more subsidy in future and some less. <br>
<br>
We have a public interest responsibility, for example to consider the welfare of 
customers with special needs and customers in rural areas, people who perhaps 
might justifiably benefit from more subsidy in certain circumstances. At the 
same time we question whether the best way to subsidise industrial projects is 
through (artificially) low electricity tariffs. Inefficiency breeds 
inefficiency. We would prefer industrial customers to have subsidy free cost 
reflective tariffs – this would allow them to manage their consumption in cost 
minimizing ways leaving the government free to provide projects with whatever 
lump sum subsidy might be justified. <br>
<br>
The Authority has implemented price controls that provide incentives to 
companies in the electricity sector to improve efficiency, and we are seeking to 
promote greater financial transparency so that the government can take 
appropriate decisions on how best to allocate resources.<br>
<br>
<b>How is the power sector doing in the country in terms of demand and supply 
and the challenges you are facing?</b><br>
Electricity is an important facilitator of economic development. Any constraint 
within the electricity sector will manifest itself as a constraint on economic 
development. The Oman Power and Water Procurement Company SAOC (PWP) is required 
by its license to ensure sufficient generating capacity is available to meet 
demand. Each year the PWP looks seven years ahead and assess its demand forecast 
against available capacity. If new capacity is needed the PWP has a duty to 
contract for it. This forward looking approach should ensure that the 
electricity sector continues to facilitate economic development – not constrain 
it. <br>
<br>
People coming to Oman to do business, particularly industrial and commercial 
customers, can look forward to a supply of electricity governed by regulations 
and security standards, and where security planning standards drive capacity 
needs, not financial constraints.<br>
<br>
To put this in context, we have a new IWPP in the construction stage at Sohar 
that is going to provide a sizeable amount of capacity to the system from next 
year. Already, whilst the new station is being constructed, the PWP has looked 
ahead and determined the need for further new capacity. The process of 
implementing a new IWPP is presently underway. We are not waiting to see how 
much capacity might be required - Oman is now subject to forward planning 
mechanisms and standards, which I believe will lead to more reliable electricity 
supply. <br>
<br>
For me, that is a significant change and is one that will signal to commercial 
and industrial projects (that drive economic development) that Oman can provide 
reliable electricity supply. As confidence in the electricity sector grows, some 
projects may decide not to build their own electricity stations and rely on 
supply from the grid. This will contribute to efficiency in resource use, and 
allow the electricity sector to capture further economies of scale. <br>
<br>
<b>How do we compare with other GCC countries, in terms of consumption per 
capita? </b><br>
The Authority’s Annual Report will include an assessment of how Oman compares in 
terms of electricity consumption to other GCC countries and other international 
markets. The benchmark for Oman is not restricted to GCC countries, but extends 
to some developed economies. On the basis of 2002 World Bank data, Oman has the 
lowest kWh consumption per capita in the GCC (3,176 kWh) and Qatar the highest 
(15,515 kWh). However, Oman’s per capita electricity consumption is higher than 
China, Brazil and Iran, but lower than the UK, Australia and the USA. It is 
difficult to understand the significance of these differences unless placed in 
some sort of context.<br>
<br>
We have been looking at 20-year series of electricity and economic data for the 
GCC and other countries in order to understand and compare differences in 
relative economic growth, changes in the structure of economic activity and how 
these, and other factors, such as population growth and local climatic 
conditions, influence the demand for electricity. Understanding past trends will 
help meet future needs. <br>
<br>
Differences in the composition of GCC populations is particularly interesting. 
Data suggests that around forty-five per cent of Oman’s population is below 19 
years of age (only Saudi Arabia of the GCC countries has a higher proportion). 
Economic development is required to provide sustainable employment 
opportunities. The government is addressing this by promoting economic 
diversification, and private sector development. Initiatives such as these have 
important implications for the electricity sector and it will be important to 
ensure diversification and industrial development are not held back by 
electricity supply constraints. <br>
<br>
<b>Infrastructure development is booming and oil prices are at an all time high 
- how are we geared up for the future? </b><br>
High oil prices can certainly contribute to short terms improvements in the 
government’s balance sheet, but in my view should not postpone or delay economic 
reforms. With a strong balance sheet the government can decide the degree to 
which it wants to promote economic expansion, such as tourism and new industrial 
projects. The government is recognizing that it has to attract activity to Oman, 
but recognizes also that excessive subsidies may not always offer the best 
prospect of sustainable value creation and economic development that are 
critical to future employment prospects. <br>
<br>
<b>The key point is that oil prices have gone up, and are funding projects and 
economic expansion – but how is the electricity sector responding? </b><br>
As mentioned earlier, the electricity sector looks forward to future needs and 
will act to ensure these can be provided in an economic and efficient manner. 
The PWP presents a seven-year demand forecast each year, the transmission 
company presents an annual assessment of the capability of the transmission 
system for the coming five years, and the electricity distribution companies 
provide annual assessments of the capabilities of their systems for the coming 
three years. Such analyses are critical to the new planning framework within 
which the sector now operates. <br>
<br>
However, demand forecasting is subject to uncertainty, and licensees have duties 
to meet reasonable demands for electricity not speculative demands. Not all 
demand will be meet unquestioned. Companies applying for electricity are 
required to provide information over proper planning horizons, and must ensure 
their forecast demands are robust. If demand is deemed unreasonable or 
speculative, the sector is unlikely to invest heavily to meet it. <br>
Increasingly, large companies and customers will be asked to sign agreements 
committing to meet the underlying costs of their forecast demands. <br>
<br>
The electricity sector should not be used to absorb the market risk of private 
sector development; we need to get the balance right. However, if there is 
genuine demand, the electricity sector is geared up to look ahead and recognize 
that demand and will work to ensure systems are in place to provide reliable 
electricity supply.<br>
<br>
<b>At this point in time, what is your estimate of how much will have to be 
invested in the electricity sector over the next few years?</b><br>
Looking out to 2020, the sector will require well in excess of RO500 million. 
Each new production facility requires the government to commit to investment of 
RO100 million. How is this investment to be funded? Well, funds could be 
diverted from alternative areas of expenditure (but this might have high 
opportunity costs), come directly from oil revenues, or from private sector 
sources. <br>
<br>
So a key driver for electricity sector reform was recognition of the huge 
investment requirements of the electricity sector over the next 10 to 15 years 
and realizing that there is an alternative available, which is private sector 
funding. One reason for an independent regulator, required by law to ensure that 
licensed private entities operating efficiently can earn an appropriate return 
on their investment, is to facilitate and attract significant investment from 
private sources. <br>
<br>
The government is clearly committed to providing subsidy, but the more efficient 
the sector, the government will have the choice of either reducing the level of 
subsidy, or reducing tariffs to pass on efficiency benefits to the customer. The 
key point is that the sector has access to investment funds needed for expansion 
without straining the balance sheet of the government. <br>
<b><br>
<img border="0" src="cs11.jpg" align="left" hspace="5" vspace="2" width="120" height="150">If, for example, Blue City, which is intending to invest hugely over the next 15 
years, approach you, how would you work out an arrangement with them?</b><br>
If a private sector investor wanted to build a power station to supply a project 
such as Blue City it would be tempting to say, let them do that. But you would 
then have a private entity providing electricity at unregulated tariffs and 
uncertain quality of supply. You would be exposing customers to an unregulated 
monopolist. And if the project’s commercial projections are wrong, how would 
investors sustain their required profitability? I suspect by charging customers 
more for their electricity, as monopolists tend to do, or perhaps by closing the 
uneconomic plant.(in which case there would be no security of supply).<br>
<br>
The sector law does not permit private power stations to supply directly to 
customers for these reasons, but instead places a duty on regulated entities to 
respond to all reasonable requests for electricity supply. <br>
<br>
In the case of Blue City, if they were to approach the sector, as they have 
done, and ask for 1.5 giga-watts of demand over a 20 year period and all 
associated transmission and distribution infrastructure, is the sector going to 
go out and fund that request unquestioningly? The answer is, and must be, no.
<br>
<br>
However, the sector will provide electricity to each project phase as the 
electricity demand of each phase materializes. The duty to respond to reasonable 
demand (as opposed to speculative forecasts) will help avoid investment in 
underutilized assets. <br>
<br>
Moreover, electricity consumers in Blue City will benefit from the protections 
offered by the sector law with regard to the regulation of tariffs and security 
of supply. <br>
<br>
The sector should not be used to absorb the market risk of private sector 
developments, but will work hard to liaise with projects such as Blue City to 
ensure that wherever a reasonable demand for electricity is identified, the 
sector fulfills all its duties and responsibilities. <br>
<br>
<b>Are you looking at non-electricity related services such as data transmission 
as well, using the existing infrastructure?</b><br>
Not at the moment. If that was going to happen, the licensed transmission 
company would have to seek the approval of the Authority and we would in turn 
assess the risks for electricity customers, the implications for quality of 
supply, standards of service, and so on. It may happen in the future, but our 
immediate focus is on improving the standards of performance and quality of 
electricity supply. </font></p>
