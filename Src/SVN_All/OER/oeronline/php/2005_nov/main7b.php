<font face="Verdana" size="3"><b>
<img border="0" src="mycar.jpg" align="left" width="200" height="133">H</b></font><font face="Verdana" size="1">yundai 
has arrived and at the rate the company is moving, you can be sure that it is 
here to stay forever. The auto giant, ranked 12th among global manufacturers in 
1998, jumped to the seventh position last year with sales of more than 3.1 
million vehicles, thanks to the company's quality obsessed chairman Mong-Koo 
Chung. When Chung took over the reins of South Korea’s largest carmaker, it was 
way behind in the auto industry. <br>
<br>
But with the launch of the 2005 Hyundai Sonata’s at the Detroit Auto Show in 
January, things started to look up. The new model from Hyundai came with an 
offer of six airbags, electronic stability control and a long list of standard 
equipment, all for less than $20,000. Today, Sonata has become the most popular 
car not inly in Oman but across the world.<br>
<br>
Hyundai has also set a new benchmark for the small SUV segment with its Tucson. 
Tucson owners have rated it higher on roominess, quietness and innovation and 
have reported that it provided them a feeling of refinement, which is typically 
not present in vehicles in this segment. <br>
<br>
With annual revenue growth of 20 per cent in the last five years, Hyundai has 
become the world’s fastest-growing automaker since 1999. Automakers world over 
are watching over their shoulder for this growing car manufacturer. Honda Chief 
Executive Takeo Fukui is quoted as commenting Hyundai is “a company we must 
watch out for”. <br>
<br>
Hyundai has set itself a target to sell 5 million vehicles globally by 2010, 
which would push it to number five rank, now held by Volkswagen. The rapid 
growth of Hyundai is attributed to an extraordinary improvement in the vehicle 
quality and by targeting the once ignored entry-level market. Hyundai is now 
giving a hard time to quality leaders like Toyota and Honda, according to the JD 
Power and Associates' annual study of initial vehicle quality. To cite an 
example, the 2004 Sonata has been declared as the most reliable car on road by 
the April Consumer Report with just two problems per 100 vehicles. <br>
<br>
Hyundai has rapidly built up regard for its products through an almost fanatical 
attention to ‘Getting It Right’. Now the company is dictating trends that others 
must follow. The success of value priced cars by Hyundai inspired General Motors 
to buy Korean company Daewoo. Hyundai's biggest aim is to push up its share in 
the US market, the largest in the world. The company is also aiming at big 
markets like Europe, China and India. Incidentally, Hyundai defeated GM and 
Volkswagen in sales in China during January-February 2005. <br>
<br>
The company is on a roll and advancing at a rapid pace. Hyundai has built a 
168,000-square-foot design and engineering center near Detroit and a $1billion, 
2-million-square-foot factory outside Montgomery, Alabama, which has started 
production. It plans to build 300,000 Sonata sedans and Santa Fe SUVs annually 
in Montgomery. This would put Hyundai closer to its target of doubling US sales 
to 1 million units by 2010. <br>
<br>
Before Chung took over, during 1980s and 1990s, the company was only aiming at 
churning out vehicles without paying too much heed to quality. Chung, who was 
then handling the after sales service and dealership, had to face a lot of irate 
customers. After the Asian financial crisis and a break up of the company 
established in 1967, his father handed over the reins to Chung. <br>
<br>
Chung was able to gather information fast and found that the quality problems 
were at the root of the company’s problems. Chung’s first step was to replace 
the top management with qualified engineers and began working meticulously to 
challenge Toyota to get ahead in JD Powers’ quality rankings. He worked closely 
with consultants and carried out extensive quality studies on weather and road 
conditions in the US in order to get closer to JD Power benchmark for the 
world’s best auto companies. He spent six years in zeroing on the defects in the 
cars. He got designers, engineers and factory mangers to work together as a team 
by creating joint committees to examine blueprints of new models and to cut down 
potential defects. This also helped to break down interdivisional barriers. <br>
<br>
Chung has also expanded his quality department by tenfold. So obsessed is he 
with quality that he’d modify a car’s design midway through its cycle that might 
even delay the launch of the model. <br>
<br>
But this has only helped to build brand loyalty among Hyundai owners and four 
out of the top five cars traded in to Hyundai dealers are from the brand, 
according to auto research site Edmunds.com. This is one reason why Hyundai has 
succeeded in many of the prime markets. <br>
In the US, Sonata offers a lower priced alternative to Toyota’s Camri and 
Honda’s Accord. In Europe, the company has increased the sales by 21 per cent 
and in India by 17 per cent, second only to Maruti, a Suzuki subsidiary. Hyundai 
has succeeded in alluring Indian customers by incorporating ingenious designs 
such as elevated rooflines in its small cars. In China, Hyundai along with 
Beijing Automotive, have sold more cars than any other automaker in the first 
two months of this year. <br>
<br>
Hyundai is not resting with improvement in quality alone. It has taken a big 
leap in sports marketing by becoming an official partner of FIFA. Its 
sponsorship of global football tournaments like UEFA EURO 2000, 2002 FIFA World 
Cup and UEFA EURO 2004 football tournaments has made it a household name in 
football loving countries. Hyundai is the official sponsor of 2006 FIFA World 
Cup in Germany. <br>
<br>
Hyundai is not alone in its drive for excellence and competition. It has teamed 
up with Kia the sisiter compant and has also improved the quality of Kia’s 
products. Now, both the companies have got a variety of products and have vowed 
to equal with Toyota-quality levels within a year and a half. <br>
