<p align="justify" dir="ltr">
<span style="font-weight: 700"><font face="Verdana" size="5" color="#FF0000">
Bancassurance: <br>
</font><font face="Verdana" size="2" color="#FF0000">
A perspective and the BankMuscat experience</font></span><font face="Verdana" size="1"><br>
<br>
<b>Saurabh Mishra focuses on the concept of Bancassurance, a relatively new 
product in the Middle East market</b><br>
<br>
</font><font face="Verdana" size="3"><b>
<img border="0" src="corp.jpg" align="left" hspace="5" vspace="2" width="120" height="112">B</b></font><font face="Verdana" size="1">ankMuscat, 
the largest banking institution in the Sultanate of Oman, completes one year of 
its maiden journey into its most recent product spin off – Bancassurance. A 
concept that is still finding its roots in the Middle East has come to effect in 
Oman after the Central Bank of Oman (CBO), the apex banking body in the 
Sultanate, opened up the entry of banks to start this new service on 9 May, 
2004. <br>
<br>
Bancassurance, a word originally sourced from the French lexicon, may broadly be 
defined as an arrangement between banks and insurance companies by which banks 
can offer insurance products to their customers. <br>
<br>
Such a wonderful convergence of the two giant industries i.e. banking and 
insurance, aligns itself perfectly with the theme of contemporary banking 
whereby more and more customers are looking forward to get into a financial 
superstore rather than a bank which can take care of all their financial needs. 
From borrowing monies to depositing the surpluses, from paying of the utility 
bills to the withdrawal of cash, from investing in capital markets to allocating 
funds for children’s future, from remitting the hard-earned monies to your 
native destinations to getting some useful financial covers to protect your 
house, car and other assets…. the list is endless.<br>
<br>
With the advent of increasing competition among local and foreign banks and a 
growing squeeze on their interest margins, there has been a constant pressure on 
the banks to stay profitable in the long run. This has given rise to the need 
for banks to look beyond their traditional means of earnings.<br>
<br>
At BankMuscat, there has been a constant effort to identify and address the need 
of the customer, well in advance. Initiative to commence bancassurance services 
in the Sultanate is yet another step towards this pursuit.<br>
<br>
<b>The Regulatory Framework for Bancassurance </b><br>
One of the key reasons behind the varied pace and nature of Bancassurance growth 
in GCC countries is the absence of a common regulatory platform. Being in its 
early stages of evolution, bancassurance is finding its feet in these countries 
in their own, unique ways and presents a whole range of models within the GCC 
itself, ranging from the ‘database activation’ model to the ‘referral’ model’ to 
the insurance sales staff-housed bank premises and so on. <br>
<br>
The Sultanate of Oman has taken a giant leap in this regard with the CBO, in 
conjunction with the Insurance Regulatory body, doing a remarkable job in 
setting up a clearly defined platform and scope for Bancassurance to establish 
and grow steadily in coming years. <br>
<br>
The policy document presents a clear role of the bank and the insurer in 
managing this service respectively, the areas of thrust and focus for each 
partner, the prominence of training of the bank staff and the awareness among 
customers are some of the points it delves into. <br>
<br>
It also highlights a strategic shift from the most prevalent approach of 
products being sold through insurance companies’ sales staff operating out of 
bank’s premises to bank’s own staff getting professionally trained and offering 
simple insurance solutions, a move that allows the customers to experience a 
truly “one-stop shop” while moving into a bank branch.<br>
<br>
<b>‘Al Rafeeq’ Bancassurance: Rapid strides to Growth and Development </b><br>
Ever since its commencement of operations on 18 July, 2004, BankMuscat 
Bancassurance has marked a steady growth in all its major areas of development, 
starting from building up a quality team of young Omani professionals to 
developing a whole of innovative and customizable solutions to its customers.
<br>
<br>
There has been a series of efforts going on with regard to product development 
and launches, integration of key bancassurance processes with that of the core 
banking systems, creating awareness amongst customers.<br>
<br>
One aspect that has been pivotal to the entire journey is the training of bank 
staff. The entire bancassurance movement hinges on how efficient and well versed 
the bank staff and its branches can be to offer this new range of services. 
Training, therefore, plays a key role towards achieving this objective. <br>
<br>
Creating awareness about bancassurance products among the common folks is the 
other major challenge that the bank is faced with at present. Long-term savings 
and protection range of products that come from bancassurance stable form the 
core to any sound financial planning exercise. In the absence of any established 
financial planning tools and infrastructure available in the country for retail 
customers, banks become the natural choice to access information, knowledge and 
advice about such products. <br>
<br>
Al Rafeeq (bancassurance range of products offered by BankMuscat) aims at 
building up a product range that would cover the whole gamut of long-term 
savings and protection needs of the customer. Protection of one’s assets and 
future earnings is the most fundamental component that should be looked at 
before preparing a sound investment plan for self and the family. <br>
<br>
Al Rafeeq products help the customer doing it in a very simple, convenient and 
systematic manner. These easy-to-understand products have been designed with 
utmost sensitivity towards every possible aspect of an average Omani family, 
their life style, their savings pattern, their social norms and beliefs, etc.<br>
<br>
To add further utility to these products, BankMuscat has added its traditional 
banking conveniences to create a delight for the customer. The bank offers an 
array of premium payment facilities/channels that takes away the hassle of 
managing track of recurring premiums, especially in case of a long-term 
savings-oriented insurance plan. <br>
<br>
<b>Key Challenges Ahead </b><br>
While most of the infrastructure (regulatory and otherwise) being in place, 
bancassurance is poised to make deep inroads and significant contributions in 
the GCC markets, both to the banks as well as insurance houses. The challenges 
that lie ahead cannot be underestimated though. <br>
<br>
Establishing this new service channel vis-à-vis other traditional forms of 
selling insurance products, awareness about the bancassurance plans and its 
usefulness, synchronization of such product prototypes with the traditional 
banking platforms and systems, harmonious integration of the two widely apart 
and seemingly distant cultures of banks and insurance houses, demystification of 
the so-called complex insurance products and above all, an effective, careful 
and close monitoring and implementation of the entire process are few of those 
key challenges that will determine the course of next steps towards this 
interesting, ambitious journey in the region. <br>
<br>
