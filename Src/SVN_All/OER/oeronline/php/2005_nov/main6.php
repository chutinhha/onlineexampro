“It is almost designed like a former British Ambassador’s residence in Muscat. I 
have never seen how that looks but I am told this is what it looks like.” Our 
“tour guide” tells us of the early reactions of the people who come here to have 
a looks like. <br>
<br>
Our “tour guide” tells us of the early reactions of the people who come here to 
have a look. “They are awestruck. They often say: Wow, we never realised Oman 
has such a resort, with such facilities!” </font></p>
