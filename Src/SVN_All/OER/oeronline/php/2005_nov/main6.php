    <p align="justify" style="margin: 10px"><font face="Verdana" size="1"><b>&nbsp;Excerpts 
    of a quick chat with Desmond Hatton, General Manager, Shangri-La’s Barr Al 
    Jissa Resort &amp; Spa, while traveling around the resort in his SUV</b><br>
    <br>
    How many people are on board right now? <br>
    At the moment, we are about 98 people. Another 200 people are coming at the 
    end of September, and 500 people coming by October. We’ll have 1,250 people 
    in all once the hotel becomes fully operational. <br>
    <br>
    <b>Will the three hotels open simultaneously?</b><br>
    Al Waha and Al Bandar will open first, to be followed by Al Husn later. So 
    we are doing it on gradual basis. <br>
    <br>
    <b>What is the rate of Omanisation here? </b><br>
    30 per cent to start with! Obviously, we are very keen on Omanisation. The 
    ministry has been extremely supportive. We took the ministry’s help on 
    finding out the right talent among Omani students. We started with 
    Omanisation last year in January. In fact, the second batch of 86 hotel 
    management students is now starting in October. Apart from that we are 
    getting lot of people from the industry (coming from hotels). It gives them 
    a unique opportunity. And we see many more opportunities coming up in the 
    coming years for those interested in the hospitality business. <br>
    <br>
    <b>How do you plan to take the Omanisation process forward as there probably 
    is not enough trained manpower available?</b><br>
    Shangri-La is very well known for its training. We already have a training 
    team on board here. As we speak now, in fact they are all going to attend 
    the orientation programme. We also have a number of people with 5-star 
    experience coming from hotels in Dubai, Bahrain and Doha. Apart from that, 
    we have approximately 250 people coming from other Shangri-La hotels. So 
    most of the people who are coming here are familiar with Shangri-La. We’ll 
    foster with new Omani students and pair them up together with somebody who 
    is very strong and train them as we go along. And I must say we are very 
    happy with the students we have got on board so far. <br>
    <br>
    <b>When do you plan to start with the dry run?</b><br>
    We plan to start the dry run in November. We are only confirming rooms in 
    January. The reason is that we want to ensure that everything is in place 
    properly. Dry runs will start with internal customers first, with the staff 
    or some of the company people. After that we’ll do wet runs with local 
    people. Then we get into full-fledged commercial operations.<br>
    <br>
    <b>When will the operations start in full steam?</b><br>
    All the hotels will start running full-steam by March, 2006, including Al 
    Husn. We are hoping that by the end of this year Al Waha and Al Bandar are 
    up and running. We are targeting a January-end soft opening for Al Husn. <br>
    <br>
    <b>Have the bookings started? Are the enquiries coming?</b><br>
    We have got a number of confirmations. Fifteen groups have been confirmed 
    already. And a lot of people are checking out for future business in the 
    hotel. We are quite confident that we, and the whole of Oman’s tourism 
    sector will do very well. <br>
    <br>
    We are getting a lot of enquiries from the Middle East as well as from 
    Europe. We believe that the GCC will be number one market for us. The number 
    two market for us will probably be the UK. The number three market will be 
    the German-speaking belt: Germany, Austria and Switzerland. And there is 
    also a lot interest being shown by the French and Italians. The Asian market 
    will come a little bit later. <br>
    <br>
    <b>How many restaurants will the resort have?</b><br>
    In all, we’ll have 20 food and beverage outlets and eight main restaurants. 
    There will be a lot of variety and the customers will get different things 
    every day at different places. <br>
    <br>
    <b>Do you plan to tap the local market for the food and beverage?</b><br>
    The local market is a huge market for us. We want something for everybody 
    here. We see that the local market will be a major contributor to our food 
    and beverage revenue as well as to the facilities. <br>
    <br>
    <b>Will the room tariff of the hotels be attractive for the average-salaried 
    individuals also? </b><br>
    In general, the hotel charges are in three different leagues. At Al Waha, 
    the charges are 90 riyals upwards, for Al Bandar it is 110 riyals upwards, 
    and for Al Husn, it’s 150 riyals upwards. We’ll have special packages for 
    the local market in the shoulder months. As for positioning, the resort will 
    set a new benchmark for the whole of Muscat, and for the whole of Oman. <br>
    <br>
    <b>How do you find Oman? </b><br>
    It’s very, very friendly and warm. I have loved it here. I am going to stay.</font></td>
