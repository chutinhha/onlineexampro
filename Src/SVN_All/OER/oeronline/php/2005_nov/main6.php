</font><font face="Verdana" size="4" color="#FF0000"><br>
</font><b><font face="Verdana" size="1">An OER team undertakes a pre-launch 
behind the scenes tour of Shangri-La’s Barr Al Jissah Resort &amp; Spa, the 
all-in-one resort complex</font></b></p>
<p align="justify" dir="ltr"><font face="Verdana" style="font-size: 14pt">R</font><font face="Verdana" size="1">esponding 
to the invitation of Alison Johnston, Director of Communications &amp; Public 
Relations, of the soon to be launched Shangri-La’s Barr Al Jissah Resort &amp; Spa, 
OER team sprang into action at the chance to “steal a look” at the progress of 
the project before it goes for a soft launch; before this juggernaut starts 
rolling.<br>
<br>
The mountain route leading to Barr Al Jissah is simply fabulous to say the 
least. In fact, the resort itself is nestled on the edge of a private bay, and 
surrounded by dramatic rock formations. It offers an astonishing sight to the 
beholder. Jagged mountains and the crystal clear turquoise sea of the Gulf of 
Oman a setting most worthy of the name Shangri-La, a Tibetan utopia in James 
Hilton's novel Lost Horizon (1933). It is also a place regarded as an earthly 
paradise, especially when involving a retreat from the pressures of modern 
civilization. The origin of the word is from Shangri (an invented name) and 
Tibetan la ‘mountain pass.’<br>
<br>
We wound our way down to their corporate office where we are met by Alison, who 
greets us with her usual charm and wit. We are duly introduced to Desmond 
Hatton, the General Manager; Desmond’s energy is infectious. In a matter of a 
few moments, we find ourselves sitting in his car. “It’ll be alright. All this 
dust will not be seen soon; it’s a only matter of time,” he assures us<br>
<br>
Before we know it, we are ushered inside the resort. We visit the rooms first. 
<br>
<br>
“Every single room here is sea facing. Every single restaurant is sea facing. 
Some rooms may have better views than others but all of them are sea facing,” 
Desmond explains. </font></p>
<p align="center" dir="ltr"><font face="Verdana" size="1"><br>
<img border="1" src="sp1.jpg" width="482" height="213"></font></p>
<p align="justify" dir="ltr"><font face="Verdana" size="1">As we move around the 
site we can’t help but feel that the resort is just about to take on its final 
form and function. Thousands of men are working to give finishing touches to the 
complex. However, contractors will be replaced by tourists, foremen will be 
replaced by bell captains and the din of construction will be replaced by the 
yelps of over excited children.<br>
<br>
We find the mattresses and televisions are still to come in, inside the rooms.
<br>
<br>
“It’s all set to be ready soon. The pool is almost ready here. We hope to start 
it next week. The grounds are all taking shape. Some big umbrellas are coming 
up,” Desmond informs. <br>
<br>
As we breeze into one of the hotel rooms, Desmond informs us that they will 
first go for a soft launch when they will have internal people as guests. After 
this they will go for the real launch. We then ask about the furniture. “The 
furniture is sourced locally from Zubair Group. Cups are coming from India. 
Kettles and Televisions are coming from Europe. It is a global attire,” Desmond 
explains. <br>
<br>
One can, in fact, feel the attempt at making the resort a complete Omani 
experience where the young and old alike can think of relishing the heritage and 
cuisine. Desmond shows the spot for evening stroll and the stage for a large 
outdoor function. He also shows us the spots for tennis courts and water sports, 
and the soon-to-come swimming pools linked together by a lazy river, all with 
stunning ocean vistas. <br>
<br>
And for those who are not particularly fond of walking that extra distance, 
there will be Volkswagen vans to transport guests from lobby to lobby, hotel to 
hotel. <br>
<br>
So why is this being touted as “the most exciting all-in-one resort complex in 
the Gulf.” <br>
<br>
The resort will offer three unique hotels. Al Husn, with 180 rooms 
and its own private beach, where the favoured few won’t have to mingle with the 
general population, Al Husn boasts luxury beyond five stars, with panoramic 
views across the resort, Al Bandar, the vibrant heart of the resort with 198 
rooms, and Al Waha, with 302 rooms, an enchanting hotel ideal for families 
offering a variety of accommodation and food and beverage experiences.<br>
<br>
The aim is to present something for everyone. Relaxation, blissful tranquility, 
indulgent therapies in one of 12 private villas in the Chi Spa, Spacious suites 
surrounded by immaculate landscaping and pristine beaches as well as a vast 
ballroom, an outdoor amphitheater, and an Omani Heritage Village, <br>
<br>
For those with 
a more active penchant, there will be water sports, desert safaris, tours of the 
forts and palaces, and upon your return an overwhelming choice of restaurants 
offering a variety of cuisines, <br>
<br>
As we proceed ahead in Desmond’s SUV, we see a 
palm tree lying uprooted amid the others standing tall. “If they don’t survive 
they go off to heaven, Desmond says. “We plan to have approximately 1,250 palm 
trees; 1,100 palm trees are in. We have date palm trees and coconut palm trees. 
And there are 74,000 plants to go in as well, ” Desmond informs us. <br>
<br>
As for the beaches, there are three separate beaches that are exclusive to the 
resort. There is a private beach of the Al Husn Hotel. The main beach is 
stone-less. And, there is a turtle beach. Lots of little walkways and features 
cross over the three exclusive beaches. <br>
<br>
Each of the hotels has got a different design. We ask about the unique colour of 
Al Husn. Desmond defends the choice of the colour: “This is the colour of the 
royalty. We wanted it to stand out. I think we have achieved the objective. 
People talk about the colour and the hotel.” <br>
<br>
The resort also plans to showcase the traditional face of Oman. “There are so 
many resorts where you don’t get a feel of the culture of the country in which 
it is located. But the directive from the owners of Barr Al Jissah Resort &amp; Spa 
was clear; that it must be authentic Oman. The Omani Heritage Village will have 
11 different shops in it, and traditional things like the henna painting, 
bread-making, silver-making, Omani weaving, Omani handicraft, and camels etc. 
Tourists can explore the village lifestyle and see how it is,” Desmond explains 
to us. </font></p>
