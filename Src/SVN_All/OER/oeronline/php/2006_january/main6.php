</font><font face="Verdana" size="3"><b>S</b></font><font face="Verdana" size="1">o 
near, yet so far! That precisely seems to be the case with the proposed - and 
much-awaited - realty funds (mutual funds in real estate) in Oman. The Capital 
Market Authority (CMA), in a landmark decision early 2005, had approved 
regulations, allowing asset management companies (AMCs) to invest in the 
Sultanate’s real estate market with the rider that they seek prior permission 
from its board. <br>
<br>
The CMA announcement was hailed as historic, with market experts terming it as 
complementary to the booming real estate market in Oman and in the region. It 
was seen as the perfect pep for Oman’s capital market. Mutual funds, in any 
case, have a sizeable corpus at their disposal. So in one smart move, CMA opened 
the vistas for a wider investor base and greater market capitalization of the 
Muscat Securities Market (MSM), thereby hoping to improve market sentiments in 
the Sultanate. And more importantly, providing the market an alternative 
platform to invest and diversify the assets and portfolios.<br>
<br>
Little wonder then that the announcement evoked widespread elation among market 
experts. But it’s been almost a year now since that announcement. Realty funds 
should have hit the Sultanate’s market by now. But there is no sign of them. <br>
<br>
The CMA announcement, in fact, had come as part of new regulations that were 
approved by the CMA Board to encourage investors to park their funds in mutual 
fund schemes. AMCs have been investing in either equity instruments or bonds 
listed on the MSM. So, with the objective of encouraging investments in mutual 
fund schemes, the government also provided income tax exemption to mutual funds 
through a Royal Decree in early 2005. <br>
<br>
The new measures, circulated among investment companies, also aimed at 
regulating mutual funds in such a way that they could enhance the confidence of 
investors and encourage saving habits in the country. The idea was to encourage 
those with modest savings to indirectly invest in the stock market, which in 
turn would check conspicuous consumption. For, unlike investments in real 
estate, mutual fund schemes are easy to liquidate.<br>
<br>
Then, why the delay in realty funds? Is it a case of under-selling the idea? 
Suggests Zaigham H. Shah, Vice-President, Asset Management, Fincorp: “I think 
the CMA has to be more forthcoming in terms of such an announcement. It should 
be creating more awareness among the fund managers and investors. Of course, 
realty funds are a good asset class but the key issue is how to structure and 
market the whole idea. CMA should put more focus on propagating the idea.” <br>
<br>
But is it not the right idea for the AMCs to come together too, like they do in 
the other markets? “I agree that we ourselves have not been very pro-active in 
terms of receiving this idea and putting it into action,” admits Zaigham. As it 
turns out, the AMCs are yet to put forth their point, collectively, to the CMA.
<br>
<br>
<b>Nascence factor</b><br>
However, some experts believe this time lag is pretty normal and reject the need 
to get ultra-active with regard to the realty funds. Says Abdul Razak Ali Issa, 
Chief Executive, BankMuscat: “I understand many of our AMCs have initiated 
feasibility studies on such products.” And he presses his point further: “Though 
the real-estate sector is currently booming across the region and definitely 
showing bullish trends in the Sultanate as well, I would not think any serious 
AMC would like to rush into a new product category without gaining a detailed 
understanding of the segment in question. I believe many could possibly be in an 
early stage of product development.” Ali is seconded by Lo’ai Badie Bataineh, 
DGM–Investment, Head of Investment Management Group, Oman Arab Bank. “Give us 
some more time. As fund managers, we need to educate our clients about the new 
developments.” <br>
<br>
Sivaraja, an analyst at Fincorp, also points out at the “nascence factor”: “In a 
developed market, the reactions will be faster and quicker. But in a developing 
market, especially where the importance of the capital markets is being felt 
slowly, the reaction does come a bit late.” <br>
<br>
<b>
<img border="0" src="sp_rep1.jpg" width="198" height="167" align="right" hspace="5" vspace="2">Structural bottlenecks</b><br>
But is there more to the delay than the nascence factor alone? What about other 
bottlenecks? Are there any more structural hindrances that have not yet been 
sorted out? Lo’ai believes there still are some roadblocks left with regard to 
the regulations. “The tax holding issue is still not clear,” he points out. And 
there are other concerns too. “We were mulling over going for real estate 
finance in Oman. But we discovered that even expatriates living in the Sultanate 
for 30-40 years can buy or own apartments or a piece of land only in notified 
areas, and not everywhere.” <br>
<br>
Adds Zaigham: “The problem is that you do not have proper real estate market in 
Oman. And fund managers do not have the expertise to buy real estate. So, there 
has to be an instrument, which is linked to the real estate. We cannot ourselves 
invent such a product. Although we have witnessed a huge boom in the real estate 
market in Oman, there is still no structured instrument where you can invest and 
get returns in an efficient manner.” Concurs Lo’ai: “I guess most investment 
companies do not have experience in real estate. In fact, Oman’s real estate 
market itself is not well developed. So if you ask me, I’ll still go for a real 
estate advisor.”<br>
<br>
Then, there is this all-important issue of investment cap. As per the CMA 
decision, AMCs have been allowed to invest only 40 per cent of their assets in 
the real estate. So the law itself needs to be amended to allow the AMCs to 
invest 100 per cent of their assets in real estate. In fact, this amendment is 
already on the agenda of the CMA Board (See interview of HE Abdullah bin Salem 
Al-Salmi, Executive Vice-President-Regulatory Affairs, CMA). 
<img border="0" src="sp_rep2.jpg" width="151" height="167" align="right" hspace="5" vspace="2"><br>
<br>
<b>Hope 2006</b><br>
However, there is still light at the end of the tunnel as far as realty funds 
are concerned. 2006 holds promise. Some realty funds might see the light of the 
day now. “It’s coming,” confirms Lo’ai of Oman Arab Bank. Maybe in the first 
quarter of 2006.<br>
<br>
BankMuscat’s CEO Abdul Razak, too, oozes optimism: “I do believe we shall see 
the leading players in the local market becoming increasingly active in this 
sector in the near future, though I would not like to be pinned down to a 
definite time frame. What we must not forget is that large real estate projects 
often have long gestation periods; the sector, therefore, requires careful 
study, particularly in the short to medium term.” <br>
<br>
At one point Fincorp was at a stage of launching a real estate fund and was 
negotiating and it has not given up as yet. Market speculation is rife that 
BankMuscat will have the honour of being the first off the starting block.<br>
Whether the realty funds hit the market sooner or later, the fact is that they 
have the potential to effect huge changes on the capital market scene in the 
Sultanate. <br>
<br>
The CMA decision has enabled local AMCs to enhance their product portfolios. As 
can be derived from the regional experience, real estate based investment 
products are attracting significant investor interest, as more and more 
investors are willing to put their money into these funds. - Mehre Alam</font></p>
