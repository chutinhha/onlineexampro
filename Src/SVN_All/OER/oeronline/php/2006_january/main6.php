    <b>
    <font face="Verdana" color="#008000" style="font-size: 16pt">
    Primer on REITs </font>
    <font face="Verdana" color="#FF0000" style="font-size: 16pt">
    <br>
    </font>
    </b><b><font face="Verdana" size="1">Dr. Vijay Gaba</font></b></p>
    <p align="justify" dir="ltr" style="margin: 10px">
    <font face="Verdana" size="3"><b>R</b></font><font face="Verdana" size="1">eal 
    Estate Mutual Funds, also known as Real Estate Investment Trusts (REITs) and 
    Listed Property Trusts (LPTs), are defined as collective investment tools 
    that pool money from investors and use the pooled capital to buy, manage and 
    sell real estate assets. These funds are common to many economies with a 
    developed property market. Generally, they are closed-ended companies or 
    trusts that hold, manage and maintain real estate for investment purposes, 
    which is leased to tenants. <br>
    <br>
    They tend to have a broad shareholder base and are often, but not always, 
    traded on a public stock. For publicly traded entities and property owners, 
    these funds provide the benefits of converting their commercial properties 
    to marketable securities thereby unlocking the investment value of such 
    properties. For the investors, these provide a viable investment alternative 
    that offers a wider investment choice and a greater portfolio 
    diversification. In the USA, REITs have been in operation since late 
    nineteenth century, Australia launched them in early 1970s, Japan and Korea 
    in 2001, France in 2003, the UK and Germany are considering introducing them 
    in 2006.<br>
    <br>
    While the first REITs and Listed Property Trusts in the US and Australia 
    have been around for some time, their growth was most impressive after the 
    1987 &quot;Black October&quot; market crash. The capitalisation of US-listed REITs 
    rose by nearly 13 times to US$155 billion between 1991 and 2001. Today, 
    about 200 publicly traded REITs with assets total over $500 billion are 
    operating in the United States. Approximately two-thirds of these trade on 
    the national stock exchanges. <br>
    <br>
    In Australia, the capitalisation of LPTs rose from A$5 billion to about A$45 
    billion between 1992 and late-2002, a rise of nine times. Starting with the 
    first LPT in 1971, there are now about 30 LPTs in Australia, accounting for 
    some 8 per cent of the total worth of listed stocks. The LPT sector has been 
    one of the strongest performing sectors of the Australian Stock Exchange 
    (ASX) since 1994.<br>
    <br>
    <b>Revenue model</b><br>
    REITs buy, develop and sell property and share profits with 
    investors/unit-holders from any capital appreciation on the sale of 
    property. Apart from sale of property, real estate funds also make money 
    from rentals on property owned by them. On the basis of their revenue model, 
    real estate funds are generally classified into the following three broad 
    categories.<br>
    <br>
    (a) Equity Funds: Equity real estate funds invest in and own properties 
    (thus responsible for the equity or value of their real estate assets). 
    Their revenues come principally from their properties' rents. In the US 
    these funds are most popular and account for more than 90 per cent of total 
    assets under management.<br>
    <br>
    (b) Mortgage Funds: Mortgage real estate funds deal in investment and 
    ownership of property mortgages. These funds loan money for mortgages to 
    owners of real estate, or invest in (purchase) existing mortgages or 
    mortgage backed securities. Their revenues are generated primarily by the 
    interest that they earn on the mortgage loans.<br>
    <br>
    (c) Hybrid Funds: Hybrid real estate funds combine the investment strategies 
    of Equity REITs and Mortgage REITs by investing in both properties and 
    mortgages.<br>
    <br>
    <b>Important factors in a vibrant real estate fund market</b><br>
    A lower interest rate regime and availability of a large pool of good 
    properties in the market are the two most important factors in emergence and 
    development of a vibrant real estate fund market. Income level and 
    investible surplus in the hands of potential investors, tax incentive 
    available to the real estate funds and investors in these funds, comparable 
    risk return ratio in other investment options like bonds, stocks, equity 
    funds, liquidity and accessibility of these funs to investors are some other 
    factor which impact the development of these funds.<br>
    <br>
    <b>Advantages of real estate funds</b><br>
    The experience of countries like the US, Australia and Japan shows that real 
    estate funds invariably result in improvement in the quality and quantity of 
    finance available for investment in commercial and residential property. 
    Besides, it allows the investors the access to a wider range of savings 
    products on a stable and well-regulated basis. Most significantly, 
    successful operation of these funds could provide tremendous support to the 
    process of structural changes in property markets to reduce costs and 
    improve flexibility and quality for tenants. From the viewpoint of 
    investors, it allows even smallest of investors to access commercial 
    property returns, otherwise available only with significant capital outlays, 
    risk of illiquidity and tax inefficiency. It is also seen to help in 
    improving stability in the property investment market by rebalancing some 
    debt with equity among property companies. These funds are normally seen to 
    be having different cycles from the bond and equity markets. <br>
    <br>
    They therefore make for prudent investment by individuals, pension funds and 
    life assurance companies wishing to reduce risk by maintaining a diversified 
    portfolio. In fact, by investing in mutual funds that operate in the real 
    estate space, an investor can periodically invest (depending on his savings) 
    in the same asset class that he finally wants to make a lump sum investment 
    in i.e. house, office or shop etc. To the corporates, it provides an 
    opportunity to release property assets from corporate balance sheets into 
    professionally managed companies. </font></td>
