    <b>In February 2005, the Capital Market Authority (CMA) had approved 
    regulations allowing mutual funds to invest in the Sultanate’s real estate 
    market. What prompted you to issue the notification? </b><br>
    In the past there were complaints from Asset Management Companies (AMCs) 
    regarding taxation, tax on dividends and capital gains and we really tried 
    our best with the Ministry of Finance and the Government in general to amend 
    the taxation law. And that happened. Now, the mutual funds are completely 
    tax-free (100 per cent). Yet, we have not seen any real development. Then, 
    it was brought to our notice that the existing law permits the funds to 
    invest in the securities market only. That’s why we came up with this 
    circular, allowing mutual funds to invest part of their equities in other 
    investments, rather than limiting them to the stock exchange only. <br>
    <br>
    <b>Between then and now, has any AMC come out with any real estate-specific 
    funds?</b><br>
    Yes, we have seen two such funds but both the funds were not really focusing 
    on Oman. They were focusing on the region (GCC and MENA). So we really want 
    some funds to be really concentrating in real estate and other investment 
    opportunities in Oman. There are plenty of opportunities in Oman and 
    liquidity as well. We need to come up with proper instruments that pool this 
    liquidity.<br>
    <br>
    <b>What kind of handholding would the CMA like to extend? </b><br>
    We, as regulators, are here to find out if there are any obstacles - legal 
    or otherwise - that might hinder the operation of mutual funds. We are ready 
    to listen to any suggestions. And we have listened to, for example, on the 
    issue of taxation and such things. We are open to any suggestions from the 
    asset managers in Oman.<br>
    <br>
    <b>Did they (AMCs) get back to you?</b><br>
    Well, they do come to us when they have any specific issues. And we do our 
    best to iron out their difficulties and come up with solutions. For example, 
    two asset management companies came to us and pointed about one of the 
    issues in the law, which when interpreted, means that mutual funds are 
    basically for investment in the MSM or in the securities market. So we went 
    to our Board to get the clarification whether mutual funds may invest in the 
    other investments as well. After the clarification from the Board, we 
    allowed the AMCs to invest up to 40 per cent of their assets in real estate. 
    But I do agree that we need to work out some sort of amendment to the law to 
    allow pure mutual funds to invest in the real estate. We have already 
    started moving in this direction.<br>
    <br>
    <b>Will you be calling the AMCs in the near future to take the process 
    forward? </b><br>
    Well, I think we need to fix our home first. And then, we will call the 
    AMCs, may be for a conference, but we need to have the law to cater for such 
    an issue. We are getting these things as, what I would call it, an exemption 
    from the law. We did allow the funds to invest up to 40 per cent of their 
    assets in the real estate. But we need to get the law amended in order to 
    allow 100 per cent of the assets to be invested into the real estate.<br>
    <br>
    <b>Are you in talks with other ministries to get this amendment moved in?</b><br>
    We have been working out the amendments here at the CMA. And discussing it 
    with the Ministry of Legal Affairs. <br>
    <br>
    <b>Could we hope to see something concrete on the realty funds soon?</b><br>
    We hope so. However, if the asset managers want to set up a fund 
    specifically for real estate, they can go through other route. Setting up an 
    SAOG company, for example. Instead of calling it a fund, they can call it a 
    company. The company law really allows that.<br>
    <br>
    <b>When is the amendment likely to come?</b><br>
    It takes some time. From the CMA, it can move in a week or a month. But the 
    process has to go through various stages - the Cabinet, and the Ministry of 
    Legal Affairs.<br>
    <br>
    <b>Where is it right now? At which stage is it currently? </b><br>
    Right now, it’s with the CMA.</font></td>
