            <p align="justify">
            <font face="Verdana" size="3"><b>B</b></font><font face="Verdana" size="1">ased on what the year 2005 saw in 
            terms of projects, I expect the tourism industry seeing a lot of 
            action. Many such tourism projects are being funded by investments 
            from the United Arab Emirates and Bahrain. All these are big 
            projects, and a big boost to the Omani economy, because the tourism 
            business was down for a while.<br>
            <br>
            On the oil and gas sector, I reckon oil prices will remain stable at 
            around $45-50/barrel. The gas sector, particularly in Oman, is 
            taking off and it is a good tiding because Oman is gas rich and any 
            revenue generation on this front will try to offset the inevitable 
            depletion in oil revenues. Right now, 500,000 barrels per day are 
            being extracted, going by the PDO figures. In 2006, this may either 
            come down or stay stable. Tough to make a call! Let us not ignore 
            the Enhanced Recovery Programme, which may perhaps help the oil 
            production. I see the Omani economy continuing to grow at a steady 
            pace. Even the rate of inflation is pegged at zero, which is good. I 
            feel that it may remain at that level in 2006 as well.<br>
            <br>
            On the question of Omanisation, many sectors have already achieved 
            100 per cent targets. These days one gets to see a lot of Omanis in 
            shops and establishments. A few months ago, it was not the case with 
            expatriates manning many such positions. However, there are zones 
            where the Omanisation programme does not apply, Knowledge Oasis 
            Muscat, for instance. <br>
            <br>
            Free Trade Zones (FTZs) are coming up, like the one at Salalah, 
            where construction activity is in full swing to build warehouses, 
            which I reckon, will be a big project for Oman. More FTZs are in the 
            blueprint stage. They are sure to generate more employment. <br>
            <br>
            Oman has been endowed with a long coastline and the container 
            terminal business is going to be another big growth driver in this 
            country. Expansion is also being mulled and linkage with the 
            hinterland will enlarge growth prospects. Big-ticket investments are 
            knocking on Oman's doors. Take, for instance, the Sohar Aluminium 
            project, a $2 billion- plus project to be completed in three years. 
            Sohar, as we know, is fast emerging as the biggest industrial 
            township in Oman.<br>
            <br>
            Real estate is another area of interest. It is a booming industry. 
            Oman is in the process of allowing foreigners to own property in 
            some of the newly developing properties. For the Omani economy as a 
            whole, things are looking pretty good. I foresee a substantial 
            growth all around. <br>
            <br>
            One does not see any cause for worry on the Rial-Dollar exchange 
            rate front. It is finely balance and the government is fully aware 
            of the dimensions. And, there are talks about a common currency for 
            the Gulf Cooperation Council members. So, there is greater 
            supervision on the currency front in view of the fact that most 
            currencies in this region are pegged to the US Dollar. Omani Rial is 
            strong and stable and there is nothing to worry about any possible 
            wild fluctuations in the exchange rate. I strongly feel that the 
            exchange rates are unlikely to witness any dramatic shifts provided 
            the oil prices hover around US$40/barrel. In the years to come, one 
            is going to witness a much more charged Oman than in the past. It 
            has purposely adopted a low profile and chose to take a long-term 
            view. <br>
            <br>
            The Sultanate was, and is, careful not to go too fast. Oman has been 
            trying to achieve a perfect balance between its cultural moorings 
            and rapid growth. They are planning better. <br>
            <br>
            When you try to compare Oman with its other GCC counterparts, you 
            get a feeling that Oman is on a slow burner. Honestly, it is not so. 
            Oman is cautious because it wants to avoid any negative 
            repercussions of fast economic growth (skyscrapers coming up all 
            over but infrastructure lagging behind!). Inadequate housing and 
            transport facility is something Oman does not want to get into. Oman 
            understands that it is taking more time to achieve a fine balance 
            and sooner it will showcase its resilience to the rest of GCC, and 
            prove that is capable of accelerating growth. I fully appreciate 
            that. <br>
            <br>
            Let us not forget that Oman is still a secret destination. Very few 
            know of its beauty. When they come to know about it, they cannot 
            help falling in love with it. All this will go a long way in 
            spreading greater awareness and giving a big boost to its tourism 
            potential. <br>
            <br>
            I see big investments coming into Oman in the near future. Many 
            potential investors have begun to see the advantages of Oman as the 
            best possible destination to unzip their purse strings and set up 
            shops. I reckon Oman as one of the most foreign investor-friendly 
            countries. I can almost give 8 out of 10 to the Sultanate on this 
            count. The country has already embarked on a privatisation drive.
            <br>
            <br>
            Many sectors are thrown open ahead of other GCC countries. Many a 
            project has foreign collaborations. It is very easy to come to Oman 
            and set up LLC type businesses. <br>
            <br>
            One can own up to 70 per cent, right from the word ‘go’. This may 
            not be possible in many countries of this region. Many potential 
            foreign investors come to Oman with the notion that their equity 
            participation cannot go beyond 49 per cent. But they are pleasantly 
            shocked to know of the ground realities. Ownership up to 100 per 
            cent is also possible if one were to get the ministerial approval.
            <br>
            <br>
            Secondly, Oman as a member of the WTO, has committed itself to 
            opening up many sectors. Many projects are already on ground. There 
            is a lot of scope for foreign participation in the power and water 
            sectors where unbundling is happening. <br>
            <br>
            In some fields, definitely, Oman lacks the right skill sets. But in 
            areas of banking, oil and gas, there are a lot of qualified Omanis. 
            At the same time, it is true that Oman is short on IT skills. But 
            they have not given up. They are trying hard by opening up training 
            institutes to impart the right skill sets that are in short supply.
            <br>
            <br>
            Omanisation and shortage of skilled labour force should not, and is 
            not, stopping many investors from looking at Oman as a suitable 
            destination. The government is open and desires discussion on all 
            aspects. That’s why I see a big scope in the coming years for Oman.</font></p>
