            Gas sector buoyant</font></p>
                <p align="justify"><font face="Verdana" size="1">
            <b>Dr Mubarak Ahmed Said, Said Al Shahry Law Office, Muscat, 
            predicts that oil and gas prices will remain stable in the coming 
            year</b></font></td>
