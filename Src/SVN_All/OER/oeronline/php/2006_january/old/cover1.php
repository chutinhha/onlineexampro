              <tr>
                <td bgcolor="#FFFCE8" colspan="2">
                <p align="center" style="margin: 10px"><i><b><font face="Verdana" size="1">Year 2006 is poised to become a historic one for the development of 
            US-Oman economic relations. A landmark event very early in 2006 will 
            be the signing (in January) of the comprehensive Free Trade 
            Agreement (FTA) between the United States and Oman</font></b></i></td>
              </tr>
            </table>
            <p align="justify">
            <font face="Verdana" size="3"><b>
            O</b></font><font face="Verdana" size="1">man 
            witnessed a stunning economic growth in 2005, due at least, partly, 
            to a number of significant new American investment projects. The 
            good news for 2006: There is no reason to think that the economic 
            growth and achievements during the coming year will not be even 
            greater, especially in terms of the US-Oman partnership. <br>
            <br>
            <b>Partners in prosperity</b><br>
            2006 is set to become a historic one for the development of US-Oman 
            economic relations. A landmark event very early in 2006 will be the 
            signing (in January) of the comprehensive Free Trade Agreement (FTA) 
            between the United States and Oman. <br>
            The FTA talks, that commenced in March 2005, were conducted in a 
            positive and friendly atmosphere, befitting the long-standing close 
            ties between the two countries. Negotiations on the text of the FTA 
            concluded in near record time in October 2005. Vice President Dick 
            Cheney and His Majesty Sultan Qaboos, in their meeting in Muscat in 
            mid-December, reaffirmed both governments’ committment to doing 
            their utmost to win ratification of the Free Trade Agreement before 
            the end of 2006. The FTA is tangible evidence that our relationship 
            is truly a “Partnership of Prosperity.” <br>
            <br>
            <b>Consumers reap benefits</b><br>
            This Agreement breaks new ground for our bilateral trade relations. 
            Once the Agreement comes into force, 100 per cent of the bilateral 
            trade in consumer and industrial products will become duty-free, 
            with a phase-out of remaining tariffs over a ten-year period. The 
            Agreement will also cover agricultural products, with a broad array 
            of such goods becoming duty-free upon ratification. <br>
            In addition to eliminating these tariff barriers, the Agreement will 
            also provide new opportunities for service providers in areas such 
            as banking, insurance, telecommunications, express delivery 
            services, and construction. With this broad commitment to opening 
            the services market, both Omani and American consumers can expect to 
            reap the benefits of lower prices and better service.<br>
            <br>
            <b>Foundation for the future</b><br>
            The Agreement further provides a broad and stable platform for 
            economic growth. For example, the Agreement requires a secure, 
            predictable legal framework for all forms of investment to occur, 
            and calls for streamlined customs procedure to facilitate trade. <br>
            Besides putting investors’ minds at ease, the Agreement also commits 
            each government to establish high levels of environmental protection 
            and ensure that its labor standards are consistent with 
            internationally recognized labor rights.<br>
            <br>
            <b>Good for diversification</b><br>
            The future is always difficult to predict, but one only has to look 
            at past performance of trade liberalization in order to see how free 
            trade agreements benefit the parties involved. Take the US-Jordan 
            FTA, for example. Trade between the US and Jordan before the signing 
            of the FTA in 2000 stood at approximately US $300 million. In 2003, 
            the figure more than tripled to over US $1 billion. During this time 
            period, Jordanian exports to the US alone rose from approximately 
            $31 million in 1999 to $694 million in 2003. The strong increases 
            have continued, as the latest figures show that US-Jordan trade 
            stood at approximately $1.4 billion over the first nine months of 
            2005. <br>
            As mentioned by the US Trade Representative and the American Chamber 
            of Commerce in Amman, a broad spectrum of Jordanian industries have 
            benefited from the FTA, including jewelry, marble and stone, 
            textiles and apparel, and electronic equipment. <br>
            <br>
            I am hopeful that we shall see similar results here in Oman, as this 
            Agreement will strengthen the already close economic relationship 
            between our two countries. The Sultanate has embarked on an exciting 
            and ambitious path of diversifying its economy, as evidenced by the 
            industrial development of Sohar, the development of its ports at 
            Salalah, Sohar, and Mina Qaboos, the burgeoning tourism industry, 
            and the creation of a free trade zone in Salalah. <br>
            <b>
            <br>
            Big names, big investments</b><br>
            We have already seen an increase in US corporate interest in Oman as 
            a result of these diversification plans. AES Barka and the Dhofar 
            Power Company, which both contain significant US investments, 
            operate first rate power and water desalination plants in Barka and 
            Salalah, respectively. Dow Chemical has teamed up with the Sultanate 
            and Oman Oil Company to form Oman Petrochemicals Industries 
            Corporation, a petrochemicals complex that will serve as an anchor 
            for Sohar. Bechtel Corporation has signed a contract with Sohar 
            Aluminum Company to construct a smelter, part of an overall $2.2 
            billion project. A number of leading US construction, credit and 
            finance firms are involved in the exciting tourism development 
            projects sprouting along Oman’s spectacular shoreline. <br>
            <br>
            With the FTA, the US can become an even more active partner in 
            contributing to the Sultanate’s growth. The opportunities that await 
            Omani companies that wish to broaden their business relations with 
            US partners are improving dramatically. <br>
            <br>
            <b>Oman: In the lead</b><br>
            By concluding this Agreement, Oman will join a select club of 
            countries that have concluded FTAs with the US. The high standards 
            that are the hallmark of our FTAs will also send a clear signal to 
            investors from all over the world that Oman is a prized partner in 
            which to invest in and with whom to do business. <br>
            <br>
            Oman has joined a special group of nations that has decisively taken 
            steps to join the US in reducing trade barriers to ultimately 
            strengthen the regional and global economy. The US welcomes Oman 
            into this group, and looks forward to seeing our trade relations 
            strengthening over the coming year.</font></p>
