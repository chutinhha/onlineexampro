            History beckons</b></font></p>
                <p align="justify"><font face="Verdana" size="1"><b>US-Oman Free Trade Agreement will benefit both countries in a 
            variety of ways, says HE Richard L. Baltimore III, 
            the US Ambassador to Oman</b></font></td>
                <td width="19%">
                <p align="center"><i><font face="Verdana" size="3"><b>
            <img border="1" src="cover2.jpg" hspace="5" vspace="2"></b></font></i></td>
