<p align="justify"><font face="Verdana" size="1"><b>Renault enters Oman<br>
</b><br>
<img border="0" src="renault.jpg" align="left" width="248" height="251" hspace="5" vspace="2">Renault, 
Europe’s leading car brand, has been launched in Oman by Suhail Bahwan 
Automobiles LLC in December 2005. A Renault car showroom located at Qurum has 
also been inaugurated. Talking on the occasion, Vishal Dhawan, MD of Suhail 
Bahwan Automobiles said, “The contemporary design, advanced electronic 
technology and top class standards in safety are going to appeal to auto buyers 
who are looking for more excitement and choice.” <br>
<br>
Renault’s range of models in Oman consists of Clio (entry level 1.4 litre), 
Megane (mid size models in 1.6 and 2.0 litres), Scenic (MPV segment) and Kangoo 
&amp; Traffic (LCV’s for trade and retail sector). <br>
<br>
Suhail Bahwan Automobiles sells Nissan range of automobiles exclusively in Oman. 
And Renault holds 44 per cent stake in Nissan. The strategic alliance between 
Renault and Nissan makes it among the top four car makers in the world with a 
sales of over 5.7 million vehicles annually and representing a share of 9.6 per 
cent in the global auto market. <br>
<br>
Globally, as reported by Reuters, Renault is expected to slash costs by at least 
1 billion euros and cut staff by up to 10 percent when CEO Carlos Ghosn unveils 
his strategy for the French carmaker on February 9. The analysts expect bold 
moves along the lines of his successful shake-up at Japanese ally Nissan. The 
group is not expected to raise its car production target beyond the current goal 
of four million cars per year but it should occupy as many niche markets as 
possible and move more towards the luxury car segment as well.</font></p>

