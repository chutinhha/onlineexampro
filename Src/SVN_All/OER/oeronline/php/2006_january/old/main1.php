    <font face="Verdana" style="font-size: 16pt"><b>Documentation was the key</b></font></p>
    <p align="justify" style="margin: 10px"><font face="Verdana" size="1">
    <img border="0" src="dream_pro1.jpg" width="80" height="96" align="left" hspace="5" vspace="2">Close 
    on the heels of sewing up the Greenfield aluminium smelter project, Sohar 
    Aluminium’s Chief Financial Officer, Paul Snyman, took time off to share his 
    thoughts with Ramesh Kumar on how he had gone about fetching money to 
    finance the project. Excerpts:<br>
    <br>
    <b>Given the size of the project (US$2.4 billion), did you find it difficult 
    to arrange for finance? </b><br>
    The strategy for arranging the finance was devised by the project’s 
    financial advisors, Citigroup, in consultation with the sponsors and Sohar 
    Aluminium. The process started with a market sounding exercise in April 
    2005, essentially a road show where the project was “sold” to the financial 
    markets. During this phase, some 30 banks and financial institutions, both 
    regional and international, were approached and requested to provide 
    indicative preliminary bids to provide non-recourse term loan facilities to 
    the project. <br>
    <br>
    Evaluation of the preliminary bids resulted in the selection of a group of 
    around 10 banks, which were requested to provide firm commitments based on a 
    fully documented finance package that had been developed by Sohar 
    Aluminium’s advisors. <br>
    <br>
    These firm bids were received in October-end and shortly thereafter, three 
    successful bidders were chosen. The three - ABN Amro, Citigroup and Sumitomo 
    Mitsui Banking Corporation (SMBC) - together with a $200 million 
    contribution by Export Development Canada (EDC), thereby provided full 
    commitment for the term loan facilities by early November 2005.<br>
    <br>
    There was an excellent response to the initial underwriting phase that 
    followed. This resulted in the group of 17 banks that eventually signed up 
    in Muscat on 11 December. The full list of MLA banks that contributed to the 
    term loan facilities includes ABN Amro, Abu Dhabi Commercial Bank, Arab Bank 
    &amp; Oman Arab Bank, Arab Banking Corporation, Bank Muscat, BNP Paribas, 
    Calyon, Citigroup, EDC, Gulf International Bank, ING, Mizuho, Royal Bank of 
    Scotland, SMBC, Société Générale, Standard Chartered and West LB. It is 
    gratifying to see the excellent support that the project enjoys from the 
    local and regional banks on this list.<br>
    <b><br>
    What kind of model of project finance have you deployed to ensure smooth and 
    swift closure?</b><br>
    All finance documentation was finalised prior to approaching the 
    short-listed 10 banks, as firm bids were the key to a smooth process and 
    swift closure. What this did was to practically eliminate the term 
    negotiation process that one would experience with other strategies. Another 
    important factor was that the project was fairly well developed in terms of 
    contractual documentation, which provided a good basis for a convincing 
    information memorandum to be presented to the financial markets.<br>
    <br>
    <b>The project is pretty huge. What about insurance against imponderables of 
    all kinds? </b><br>
    Insurance covering all the major perils that could arise during the 
    construction phase and that the company and its sponsors deemed necessary to 
    insure against, has been secured. Placement of the insurance in local and 
    international markets was expertly managed by the Al Ahlia, a Muscat-based 
    insurance company, that was selected in a competitive bidding process and 
    was well supported by Marsh, their reinsurance brokers.<br>
    <br>
    <b>According to documents, RO563.5 million will come from international and 
    regional banks and financiers in the non-recourse term loan facilities. What 
    is the dispersal schedule? When is the first draw down of this lot expected?
    </b><br>
    The term loan facilities totaling US$1.460 billion (RO 562 million), 
    together with a guarantee facility for US$85 million, have been catered for 
    by US$200 million from Export Development Canada, US$ 260 million Islamic 
    finance and US$1.085 billion commercial loan facilities. The debt will be 
    drawn down over the three-year period of construction with the first draw 
    down of some US$95 million due to be received by the company towards the end 
    of December 2005. Repayments will be spread over a period of 12 years 
    following the project’s completion.<br>
    <br>
    <b>A sum of US$240 million (almost 10 per cent of the total project finance) 
    is being raised through the Islamic banking route. Is it a single party 
    Islamic financing deal, or a consortium of Islamic bankers? </b><br>
    This is the first time that Islamic Finance has been used for a capital 
    project in Oman and it paves the way for access to this source of funding 
    for future projects in the country. As many as 11 of the 16 MLA banks have 
    Islamic financing capabilities and were able to provide a portion of their 
    commitment in this form.</font></td>
