<p align="justify" dir="ltr"><b><font face="Verdana" style="font-size: 16pt">
<br>
</font></b><font face="Verdana" size="5" color="#FF0000"><b>Dream Project</b></font><font face="Verdana" size="1"><br>
<br>
<b>The landscape of Sohar Industrial area will never be the same again, with the 
commencement of multi-billion Sohar Aluminimum project taking off, ably 
supported by Alcan of Canada, writes Ramesh Kumar</b><br>
<br>
<img border="1" src="dream_pro.jpg" width="250" height="151" align="right" hspace="5" vspace="2"></font><font face="Verdana" size="3"><b>B</b></font><font face="Verdana" size="1">arely 
12 months ago, it did not exist. Today, it does. In concept and blueprint, if 
not in physical terms! The first Greenfield aluminium smelter project in over a 
quarter century in the entire Middle East, Sohar Aluminium is the port city’s 
dream project. The project’s size – US$2.4 billion (RO 926 million) – makes it a 
mammoth one. No wonder the smile on the face of Ahmed Al Wahaibi, Chairman of 
Sohar Aluminium, was rather wide on December 11, 2005 – a Red Letter Day, no 
doubt – as wannabe equity and technical partners gathered in Muscat to put their 
seal of approval to go ahead with the development of the project in Sohar.<br>
<br>
“It is quite incredible when I think that a little over a year ago, Sohar 
Alunimum did not exist. Today, we have a core team of 150 of the best people in 
the industry, ready to take this project forward,” gloats Tony Kinsman, Chief 
Executive Officer, Sohar Aluminium. His excitement is understandable. Given the 
size of the project and technical and other requirements, Kinsman and team 
literally burnt the midnight oil to hasten the process. <br>
<br>
<b>Suitable partners</b><br>
Roping in suitable partners was a daunting task for Sohar Aluminium project 
heads. Take a look at the shareholders to understand the enormity of the task at 
hand: Global aluminium giant Alcan of Canada has a 20 per cent equity in the 350 
kilotonne per year smelter, which is expected to begin production in the third 
quarter of 2008. State-owned Oman Oil Company SAOC is the major partner and Abu 
Dhabi Water &amp; Electricity Authority (ADWEA) is the third stakeholder in the 
prestigious project that will be using the proven AP35 technology, being used in 
a newly constructed smelter for the first time (See Box: Top Technology). <br>
<br>
Alcan Primary Group’s President and Chief Executive Officer, Cynthia Carroll, 
who was present on the historic day of project agreement signing and financial 
closure, said Sohar Aluminium would be a “world class project”. <br>
<br>
Sohar Aluminium was conceived in September 2004 and the equity is shared among 
Oman Oil Company, ADWEA and Alcan in 40:40:20 ratios. Bechtel has been appointed 
as the Engineering, Procurement and Construction Management (EPCM) contractor of 
the smelter. European power giant Alstom will build a 1,000-MW capacity power 
station with the use of combined cycle turbines. ADWEA has chipped in to provide 
technical assistance in the construction and operation of the power plant. <br>
<br>
Soon after the Oman government began planning the Sohar Aluminium project, Oman 
Oil and ADWEA jumped onto the bandwagon. As per plans, the Greenfield project 
will be based near the existing Sohar Industrial Area. It is expected to provide 
a major economic boost to the Al Battinah Region of Oman. With the construction 
of a new Sohar Aluminium-owned 1,000 MW power plant, the smelter will have a 
long-term access to dedicated power supply on competitive terms and in a 
quantity that is sufficient to meet its energy requirements.<br>
<br>
Abu Dhabi’s intention to participate in the project was two fold. Primarily, 
this is a strategic project representing a “milestone in the path of bilateral 
brotherly joint cooperation between Abu Dhabi government and the Sultanate of 
Oman,” as HH Sheikh Diab Bin Zayed Al-Nahyan, Chairman of ADWEA, put it at the 
historic signing ceremony. Secondly, ADWEA’s expansion strategy comes as a 
result of its success in the privatization of the power generation sector. ADWEA 
is confident that such participation in the region will create the preliminary 
platform for the prospective GCC economic integration.<br>
<br>
<b>Money matters</b><br>
The other big challenge came in the form of procuring money to finance the 
mammoth project. December 11, the project-signing day, also witnessed global 
financiers taking part in this prestigious project. International and regional 
banks and financiers signed on the dotted lines to provide US$1.4 billion 
non-recourse term loan facilities. Significantly, Sohar Aluminium also bags the 
distinction of being the first project in Oman to avail of Islamic finance. A 
sum of US$240 million will come through the Islamic finance route. “We have the 
best technology, a first of its kind from Alcan and we also enjoy the 
distinction of bringing in the Islamic financing to Oman,” chuckles Kinsman.<br>
<br>
But for Chief Financial Officer Paul Snyman, arranging money at this scale is 
child’s play. He has been the key figure in several gigantic projects in the 
past. Chartered Accountant by profession, Snyman has more than 20 years of 
experience at CFO level in the mining and minerals industry. After serving a 
slew of mining giants in South Africa and Mozambique, he joined Sohar Aluminium. 
What’s the key to his success in sewing up financiers in the shortest possible 
time? “All finance documentation was finalised prior to approaching the 
short-listed 10 banks, for firm bids, this was the key to a smooth process and 
swift closure,” avers the financial wizard (See the Interview). Not only that, 
Snyman has to work extra hard to ensure that the project was adequately insured 
against all imponderables for which he roped in Al Ahlia, a Muscat-based 
insurance company ably supported by Marsh, their reinsurance brokers. <br>
<br>
Chairman Wahaibi is equally bullish: “With all ingredients of success, stable 
environment, government incentives, competitive gas and raw material supply 
contracts, strong sponsors and a management team that combines a wealth of 
expertise and experience in leading projects around the world, Sohar Aluminium 
will set a new benchmark for aluminium smelters around the world.” Kinsman too 
is excited about the project. The project will go on stream in the middle of the 
Seventh Plan (2006-2010) and is expected to make a significant contribution to 
Oman’s economy by creating job opportunities for Omanis and by creating 
opportunities for support services and downstream industries. Kinsman is also 
confident that Sohar Aluminium will emerge as the employer of choice in the days 
to come. </font></p>
<p align="justify" dir="ltr"><font face="Verdana" size="1"><b>Business plan</b><br>
What is the business plan? “Initially, we plan to produce only remelt metal in 
the form of ingots and sows which will be exported,” explains Kinsman. In time, 
Sohar Aluminium hopes to develop its casthouse to produce alloys and shapes 
suitable for local downstream manufacture. “We have an ideal situation where 
downstream industries can be located adjacent to the smelter. Hot metal can be 
transported direct from the potlines to our customers thereby saving on the 
costing of cooling the metals and later reheating it,” he adds. <br>
<br>
Meanwhile, Kinsman is not unduly perturbed over meeting the self-imposed 
deadline of ensuring the project goes on stream in mid 2008. “We have the money. 
We have the best technology partners and the best support from all quarters. 
Where is the worry?” he asks rhetorically. <br>
<br>
As per the plans drawn up, preliminary work at the site has already commenced 
and actual construction is expected to begin in the second quarter of 2006. 
Kinsman is confident that pre-commissioning activities will be carried out end 
2007 and come mid-2008, and the first batch of aluminium should roll out. Not 
too far away. Only 30 months. For a greenfield project of such gigantic 
dimensions, such timelines are nothing unusual. It is worth waiting. </font></p>
