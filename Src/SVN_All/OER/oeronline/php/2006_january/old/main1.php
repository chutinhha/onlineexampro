        <p style="margin: 10px" align="justify"><font face="Verdana" size="1">
        One of the major highlights of the Sohar Aluminium project is its 
        technological excellence. The company, as part of first phase 
        development, will build and deploy a single AP35 potline, together with 
        associated carbon and casting facilities. It is considered to be the 
        best-proven technology available viz., Aluminium Pechiney’s AP35. 
        Moreover, it will be the first commercial use of this level of the AP 
        technology, which will be used for a full smelter project. No wonder 
        that Alcan of Canada is pumped up because it is “the venture” for them 
        in the region.<br>
        <br>
        According to Marco Palmiere, President of Alcan Primary Metal Group in 
        the Asia Pacific region, the AP35 technology, till now, has been used in 
        the upgrade of three existing smelters around the world. “This is the 
        most advanced, efficient and environmental-friendly technologies 
        available,” he claims. Alcan is a multinational, market-driven company 
        and a global leader in aluminium and packing. With world class 
        operations in primary aluminium, fabricated aluminium as well as 
        flexible and specialty packing, aerospace applications, bauxite mining 
        and alumina processing, Alcan today is well positioned to meet and 
        exceed its customers’ needs for innovative solutions and service. <br>
        <br>
        Simply put, what does it mean for Sohar Aluminium? “We are committed to 
        building it safely without any adverse impact on the environment. The 
        AP35 technology is an incremental improvement on the widely used AP30 
        technology,” points out Tony Kinsman, the CEO, and hastens to add that 
        this technology has been used at dozen or more potlines across the world 
        during the past 15 years.<br>
        <br>
        Moreover, the Sohar Aluminium potline will be a little longer with 360 
        pots – as against the previous longest of 336 pots. What is the 
        advantage? According to Kinsman, together with the latest technology and 
        the longest potline, Sohar Aluminium will be able to churn out 350,000 
        tonnes of aluminium per year, which he proudly proclaims, will be the 
        highest production from any existing potline in the world.</font></td>
