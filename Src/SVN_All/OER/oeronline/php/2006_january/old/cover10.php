            <font face="Verdana" size="3"><b>T</b></font><font face="Verdana" size="1">he 
            tourism sector in Oman has been seeing steady development over the 
            last five to ten years, but the extraordinary growth of the last 18 
            months has been exceptional and bodes well for the future. <br>
            <br>
            The reasons for this explosion are simple: firstly, the Government 
            has publicly stated that tourism is a key focal area of Oman, a 
            proactive Ministry of Tourism has been created and investment has 
            been made to position and promote Oman as a country of warmth, 
            heritage and natural beauty. <br>
            <br>
            Secondly, the emergence of Oman as a destination has coincided with 
            stagnation and dissatisfaction on other, previously popular 
            destinations. Dubai has experienced very rapid, dramatic growth, but 
            at what cost? Roads are congested, visa applications at the airport 
            take time and hotels seems to appear like mushrooms overnight in a 
            cacophony of scaffolding, pile driving and drilling. For visitors 
            looking for a more natural, and stimulating experience, Oman is a 
            popular choice!<br>
            <br>
            The development of the sector will depend on two factors. Firstly, 
            the continued spread of fine hotels and accommodation options. <br>
            <br>
            An additional focus in coming months must be an increase in the 
            number of direct, non-stop flights from key international source 
            markets. This has already started, but slowly, with many of the 
            major international carriers seeing the undoubted potential of Oman 
            as a tourist, incentive and conference destination.</font></p>
