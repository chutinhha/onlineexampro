                <td bgcolor="#FFFCE8" colspan="2">
                <p align="center" style="margin: 10px"><font face="Verdana" size="1"><b><i>The 
            coming year promises to be all the more exciting for key 
            developments that promise to impact the banking sector both directly 
            and indirectly in the Sultanate: the year will act as a watershed 
