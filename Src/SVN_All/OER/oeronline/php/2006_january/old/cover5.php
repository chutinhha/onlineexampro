            <p align="justify">
            <font face="Verdana" size="3"><b>T</b></font><font face="Verdana" size="1">he year 2006 promises to be an extremely exciting year for the 
            banking sector in Oman. Over the past 35 years, the Sultanate has 
            changed from a land blessed with plenty to a nation that is among 
            the most forward looking of the Gulf States, thanks to the visionary 
            leadership of His Majesty Sultan Qaboos bin Said and the progressive 
            policies of His Majesty’s government. <br>
            <br>
            The banking sector, under the able guidance of the Central Bank of 
            Oman (CBO), has worked hand in hand with the government to 
            facilitate this change. With regional economies continuing to show 
            unprecedented growth on the back of strong global energy prices and 
            increased geopolitical stability, and GCC states moving closer 
            toward the creation of a common economic block and the sharing of a 
            common currency, the role of the sector as a catalyst of change is 
            only expected to assume greater significance during 2006. <br>
            <br>
            <b>Watershed year</b><br>
            The coming year promises to be all the more exciting for three key 
            developments that promise to impact the banking sector both directly 
            and indirectly in the Sultanate: the year will act as a watershed 
            year, with the WTO regulations coming into play at a national level 
            and the Basel II regulations being implemented by the banking 
            sector. It will also witness the signing of the FTA between Oman and 
            the US.<br>
            <br>
            While the Basel II regulations are essentially an issue that will 
            impact the banking sector alone (and will be largely dictated by the 
            guidance issued by the CBO), the WTO regulations and the FTA are 
            another matter altogether. For one, they promise to help the country 
            integrate much farther into the global economy in line with the 
            vision of His Majesty, and two, they are also expected to result in 
            the emergence of substantial opportunities for local industry to 
            extend their competencies overseas. <br>
            <br>
            Finally, they will also result in many more global organizations 
            looking at the Sultanate and its dominating youth market as a 
            potential destination on the global map. <br>
            <br>
            <b>Local market dynamics</b><br>
            The impending changes are all the more exciting in Oman where the 
            performance of the banking sector has historically depended upon the 
            health of the Omani economy, as the sector benefits directly from 
            the increased economic activity in the country from government 
            spending With the country’s proven oil reserves expected to last for 
            approximately 20 more years, the government is expected to continue 
            to press forward with its Vision 2020 plan during the year ahead. 
            The visionary policy of broadening the country’s industrial base and 
            promoting economic growth other than through petrochemical 
            activities has at its core three key elements: diversification, 
            privatisation and education.<br>
            <br>
            The diversification programme is rooted in the creation and 
            expansion of Oman’s industrial base in a number of sectors. 
            Accordingly, the programme features a number of large-scale 
            projects, including expansion of the Oman Liquid Natural Gas plant 
            at Qalhat, the Oman-India fertilizer project, the Oman Polypropylene 
            Plant, the Oman Methanol company and the Sohar Aluminum smelter, as 
            well as iron and steel projects. <br>
            <br>
            Approximately US$ 7 bn in infrastructure projects are planned over 
            the next two years. In addition, the government is focused on 
            increasing tourism in Oman and has announced the construction of a 
            new terminal at Muscat’s Seeb International Airport. Over US$ 3.5 bn 
            of tourism projects, such as new hotel and real estate development 
            are also being planned across the country in the medium term.<br>
            <br>
            The second key aspect of the government’s plan comprises continued 
            privatisation of publicly owned companies. In July 2003, the 
            government sold a portion of its shareholding in Oman Flour Mills 
            and Oman Cement. In April 2004, it sold the bulk of its 65 per cent 
            shareholding in Al Maha Petroleum. In addition, in June 2005, 
            Omantel, Oman’s telecommunications operator, was partially (35 per 
            cent) privatized in the largest initial public offering in Omani 
            history.<br>
            <br>
            The third key aspect of the government’s plan consists of 
            substantial investments in human capital and the Omani education 
            system to meet the needs of the country’s rapidly changing economy, 
            with particular focus on vocational training and academic fields 
            with practical applications.<br>
            <br>
            <b>Banking sector in a rapidly changing economy</b><br>
            We believe that Oman’s banking sector is well positioned to respond 
            to the shifting conditions in the Omani economy. While interest 
            income continues to form the primary revenue source for the sector, 
            there is an increasing realization that the future, perhaps, lies in 
            also giving due importance to developing expertise in fee and 
            commission based operations. <br>
            <br>
            This is expected to result in the growing importance of a diverse 
            range of banking products, services and business lines that are in 
            the ‘value-added’ or ‘knowledge space’ and consequently have the 
            potential to earn substantial fee and commission incomes on a 
            recurring basis. <br>
            <br>
            With the growing pace of economic, industrial and technological 
            reform in the country, there will be a need to develop a full range 
            of banking services for both the retail and corporate banking 
            markets, many of which are yet to realize their full potential.<br>
            <br>
            What is also expected is increasing levels of competition in the 
            banking sector with more and more regional and international banks 
            making their presence felt in the country and large Omani banks 
            gearing up further to make their presence felt across the region. 
            This can only augur well for the sector, which is already focused on 
            providing greater value to the customer. <br>
            <br>
            Heightened competition will ensure greater attempts at 
            consolidation, value-creation and higher efficiency levels. The 
            consumer in particular and the banking sector in general has only to 
            gain from such a move.</font></p>
