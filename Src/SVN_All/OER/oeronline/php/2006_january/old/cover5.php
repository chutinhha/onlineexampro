            Banking on trust</font></p>
                <p align="justify"><font face="Verdana" size="1"><b>AbdulRazak Ali Issa, Chief Executive, BankMuscat, forecasts 2006 as 
            a watershed year for banking industry in the Sultanate, with the 
            Basel II regulations set to be implemented by the <br>
