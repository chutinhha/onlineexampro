                    <b>Sumitomo acquires majority stake in Renaissance 
                    subsidiary</b><br>
                    <br>
                    Renaissance subsidiary, Topaz Energy and MARINE Ltd. (TOPAZ) 
                    sold an 80 per cent shareholding 
                    <img border="1" src="sumitomo.jpg" align="left" width="200" height="114">in Mezon Stainless Steel 
                    FZE to Sumitomo Corporation of Japan for AED 36,000,000 in 
                    cash. The transaction was completed in December and a 
                    significant capital gain will be recorded by the parent 
                    company. Mezon Stainless Steel FZE is recognized as one of 
                    the leading stockist of stainless steel products for the oil 
                    &amp; gas industry in the region and operates a 5,500 sqm 
                    warehouse facility in the Jebel Ali Free Trade Zone.<br>
                    <br>
                    The management announced the sale following a strategic 
                    review of its businesses. “Mezon has been an excellent 
                    business for us but it is not central to our long-term 
                    direction,” said Topaz CEO Anjan Mitra. The divestment has 
                    been made with the strategic intent of focusing on Topaz’s 
                    core marine and oil &amp; gas services-related business (which 
                    has expanded significantly following the acquisition of BUE 
                    Marine). Apart from realizing value for the enterprise it 
                    will also unlock funds otherwise deployed in the associated 
                    working capital of this trading business. Topaz is currently 
                    engaged in a major vessel construction program to take 
                    advantage of robust growth in the offshore oil &amp; gas sector.<br>
                    “Sumitomo has had a long and successful association with 
                    Topaz and we are delighted to take our partnership to a new 
                    level by acquiring a majority stage in Mezon” said Kazutoshi 
                    Fujii of Sumitomo Corporation. “Sumitomo is a global 
                    supplier of stainless steel products and we believe this 
                    transaction will allow Mazon to grow further by expanding 
                    its market reach and product lines” said Mitra. <br>
                    <br>
                    Mezon parent company, Topaz Energy and Marine Ltd., is one 
                    of the leading Dubai based oil &amp; gas services company. Topaz 
                    was acquired earlier this year in an all paper offer by 
                    Renaissance Services SAOG.</font></p>

