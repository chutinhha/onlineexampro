                        <b>Doha Bank implements anti money laundering system<br>
                    <br>
                    </b>Doha Bank, the best performing bank in Qatar, has 
                    <img border="1" src="doha_bank.jpg" align="right" width="200" height="105">completed the process of deploying cutting edge anti money 
                    laundering technology systems across the enterprise to bring 
                    the Bank in lines with the best practices being adopted by 
                    the global financial industry. Doha Bank has partnered with 
                    the region’s leading software integrator IMTAC to deploy 
                    InfrasoftTech’s OMNIEnterprise: anti money laundering 
                    solution to safeguard against suspicious financial activity 
                    and money laundering. In doing so, Doha Bank also becomes 
                    the first bank in Qatar to deploy an enterprise wide, anti 
                    money laundering solution. <br>
                    <br>
                    The AML system deployed across the financial institution 
                    marks an important step in Doha Bank’s ongoing technology 
                    advances, and is also in line with the Bank’s commitment to 
                    its investors and shareholders in creating an international 
                    standard banking environment. This system uses a powerful 
                    combination of business intelligence based pattern 
                    detection, customer profiling, filtering and transaction 
                    analysis to alert the bank against suspicious financial 
                    events.<br>
                    <br>
                    As the Bank completes a host of technology initiatives, 
                    Seetharaman, Acting General Manager, said, “Doha Bank is 
                    committed to ensuring that the Bank remains as one of the 
                    leading technology adopters in the Middle East region and is 
                    happy to partner in this endeavor with leading technology 
                    companies like Imtac &amp; InfrasoftTech. Doha Bank is also 
                    committed to the implementation of anti money laundering 
                    solutions and policies in the Bank in lines with the 
                    international banking best practices and creation of a 
                    secure bank for its customers, shareholders and investors.”<br>
                    <br>
                    Ashok Sardiwal, CEO – Imtac said “Doha Bank was a strategic 
                    addition to our customer accounts in the region. The active 
                    involvement and support of the project team from Doha Bank 
                    together with our consultants has resulted in completing the 
                    project with in the stipulated time frames”. <br>
                    <br>
                    Hanuman Tripathi - CEO – InfrasoftTech said “ We consider 
                    this opportunity to work with Doha Bank as an endorsement of 
                    InfrasoftTech’s ability to deliver an international quality 
                    product in a fast track project. We are confident that the 
                    deployment of OMNIEnterpise: anti money laundering solution 
                    will help Doha bank in reducing its operational risk 
                    indicators and also help it counter regulatory risks with 
                    respect to AML measures across geographies”<br>
                    <br>
                    “The successful implementation of AML system has crossed yet 
                    another milestone in the continuous adaptation of leading 
                    technology systems enabling Doha Bank to be the leader in 
                    market acquisition and innovative customer services”. 
                    Krishna Kumar, Head of IT said. “The Omni Enterprise AML 
                    system implementation was one of the fast-track 
                    implementation we have achieved with the greater 
                    co-ordination of its project team members”. Khalid Sherullah, 
