                    Restall, CEO of Salalah Free Zone Company, talked abut the 
                    importance of successful free zone planning and 
                    implementation in the economic growth of a country. He 
                    shared his three-decade long experience of working on free 
                    zones in the Middle East. He captivated the audience by 
                    sharing the vision of Salalah free zone and how it can 
                    transform the fortune of the Sultanate.</font></p>

