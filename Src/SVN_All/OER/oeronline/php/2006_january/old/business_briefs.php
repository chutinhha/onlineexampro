                    <b>Branding Oman report ready<br>
                    <br>
                    </b>Aimed at creating a new brand identity for the Sultanate of 
                    Oman, Branding Oman, the country branding report is ready 
                    for review by the committee formed for this project. HE 
                    Maqbool bin Ali Sultan, Minister of Commerce and Industry, 
                    heads the committee. <br>
                    <br>
                    The Ministry of Commerce, together with Ociped, had signed a 
                    deal with Landor Associates, a global branding major, to 
                    promote four economic sectors, including tourism, 
                    manufacturing (trade), education and ICT in targeted 
                    markets. <br>
                    <br>
                    The other core members of this Branding Oman panel are drawn 
                    from the Ministry of Tourism, the Ministry of Transport and 
                    Communication, the Ministry of National Economy, the Royal 
                    Oman Police, the Ministry of Finance, and Oman Chamber of 
                    Commerce and Industry (OCCI). <br>
                    <br>
                    Branding Oman project is part of the vision to diversify the 
                    national economy from oil to non-oil based revenues and 
                    promoting the image of Oman to the world not only as an 
                    authentic Arabian tourism experience but also as a hub for 
                    investment technology trade and education for the whole 
                    peninsula.</font></p>

