                    <b>Omantel, TWA Pakistan sign $1.2m deal <br>
                    </b><br>
                    Omantel has signed a landing-party agreement with Pakistan's 
                    Trans World Associates (TWA) to lay a submarine cable from 
                    Seeb to Karachi. The contract involves a total outlay of 
                    $1.2 million. In line with this agreement, Seeb landing 
                    station will be upgraded to a multiple link hub and a main 
                    station for Falcon, FLAG and TWA. It will also elevate 
                    Omantel status as a regional multi-cable operator and will 
                    open up new business horizon within Oman and within the 
                    region. <br>
                    <br>
                    Work on the project will commence in February 2006, and the 
                    agreement is valid for 15 years. TWA has also shown an 
                    interest to acquire a capacity from Omantel on 
                    Falcon/FLAG/SMW3. It will benefit Omantel in leasing such 
                    capacities either to TWA or any other potential operators.
                    <br>
                    <br>
                    Omantel and TWA are also undertaking a feasibility study to 
                    leverage Omantel's world standard Internet facilities to 
                    provide customers with IPMPLS services such as e-government, 
                    e-business, data warehouse and VPN solutions to the 
                    ministries, banking and corporate sectors.</font></p>

