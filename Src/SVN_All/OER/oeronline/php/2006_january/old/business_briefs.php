                    <b>Work on Blue City to start<br>
                    </b><br>
                    <img border="1" src="blue_city.jpg" align="right" width="150" height="121">“Giving birth to a brand new city is no easy task. But 
                    despite the fact that we announced this massive development 
                    only in June, we are on track to start work at the end of 
                    2005”, said Anees Bin Essa Al Zadjali, Chairman of ASIT (Al 
                    Sawadi Investment and Tourism Co), Blue City’s development 
                    company. <br>
                    <br>
                    “Much credit for our steady progress must be given to many 
                    Omani authorities and Ministries with whom we are working 
                    very closely. We have made great strides towards meeting our 
                    goals on schedule and there will be news of our 
                    achievements,” he added. The 35 sq km Blue City centred at 
                    Al Sawadi (100 kms northwest of Muscat) is expected to take 
                    15 years and $15 billion to complete over several phases. 
                    Work on the $1.9 billion Phase One is scheduled to start 
                    soon. According to the company, the environmental effects of 
                    Blue City’s land-based construction have been studied by the 
                    international consultants WS Atkins International &amp; Co. A 
                    strategic Environmental Impact Assessment (EIA) study was 
                    submitted at the end of October. An initial “Scoping” study 
                    had been commissioned earlier and was completed in July. 
                    Throughout its research, WS Atkins worked closely with 
                    Oman’s Ministry of Regional Municipalities Environment and 
                    Water Resources (MRMEWR) to determine the core issues 
                    surrounding implementation of Phase One. </font></p>

