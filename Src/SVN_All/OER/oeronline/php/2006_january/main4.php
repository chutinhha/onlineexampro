</font><b><font face="Verdana" style="font-size: 16pt">
<br>
</font></b><b><font face="Verdana" size="5" color="#FF0000">GCC on a trot again</font></b><font face="Verdana" size="1"><br>
<br>
</font><font face="Verdana" size="3"><b>A</b></font><font face="Verdana" size="1">s 
the six founder-member Heads of States representing their respective countries 
at the conclusion of the 26th Gulf Cooperation Council (GCC) Summit held in Abu 
Dhabi disassembled, there was a big sigh of relief. Not that the Conclave 
reached any far-reaching conclusions that may usher in world peace or bestow 
prosperity. The biggest relief was that the members managed to overcome internal 
differences over economic issues to move forward – albeit in small steps. 
Something is better than nothing, perhaps.<br>
<br>
The most promising outcome of the GCC Summit was the unanimous decision to unify 
foreign trade policies for the GCC countries so as to enable them deal with the 
outside world as a single economic bloc. In effect, this may translate into a 
single market for the entire GCC comprising of Saudi Arabia, the United Arab 
Emirates, Bahrain, Qatar, Kuwait and Oman, something on the lines of the 
European Community. In the absence of a unified trade policy, movement of goods 
and services among the member countries itself will pose a challenge, leave 
alone trading with the external world. By December 2007, the members are 
expected to complete all formalities. <br>
<br>
As part of this exercise, the Summit leaders put their seal of approval on 
moving ahead with the objectives of Customs Union. To begin with, a charter of 
52 indigenously produced items has been approved for exemption from Customs duty 
among the member states. With the passage of time, it will expose the GCC 
countries to the practicalities of implementing the Customs Union, and as they 
become more comfortable, leading to the enlargement of the charter beyond the 
initial 52 items for exemption. In the process, many of them will be able to 
figure out their competency level in chosen areas and leverage that as the 
competitive advance. Sooner rather than later, GCC countries may be able set up 
employment bureaus, rent-a-car outlet business and promote cross-cultural 
activities without much ado. <br>
<br>
According to experts, the creation of a single economic bloc is not going to be 
an easy task. Though most of the member countries are endowed with oil, which 
incidentally is driving their respective growth, there is a growing realization 
for the need to look beyond oil revenue. Such a diversionary strategy has led to 
skewed growth projections among the member nations.<br>
<br>
The International Monetary Fund (IMF), for instance, claims that the UAE is 
emerging as the third largest economy in the Middle East – closely behind Saudi 
Arabia (a GCC member) and Iran. Experts claim that the rapid growth in the UAE 
economy is due to the sharp increase in its crude production, and steady 
expansion in the non-oil sector etc. Qatar too seems to be a frontrunner in 
terms of per capita income, thanks to its high GDP and relatively small 
population. It is witnessing a sharp rise in its LNG exports. Oman, certainly, 
is performing well, warranting the same IMF to praise its declining reliance on 
oil revenue and increased promotion of non-oil avenues.<br>
<br>
So, it is no secret that the GCC members are at various stages of economic 
development. Naturally, this has led the Summit leaders to advise the governors 
of the respective Central banks to determine the benchmarks, economic indicators 
and other parameters to set the ball rolling for the creation of a single market 
by 2007. The Council also approved the monetary and financial criteria agreed 
between the economic and financial committee and the central banks and financial 
institutions committee. These criteria seek to achieve monetary and economic 
integration. <br>
<br>
Interestingly, the idea of an integrated rail network linking all member 
countries of the GCC has got a further boost with the Summit leaders approving 
to move ahead with the proposal. Efforts have already started to synergize 
multi-model transportation and economic growth and regional integration. Close 
on the heels of rail linkage, water connectivity is also on the cards. <br>
<br>
Things are moving in a positive direction for the region. In the past few weeks, 
Oman and Saudi Arabia joined the ranks of the World Trade Organization. The 
negotiations between the GCC and the European Union on Free Trade Agreement is 
moving smoothly. The entire region is witnessing an economic boom with more 
investment pouring in and foreign investors evincing greater interest to put 
their hard earned money in infrastructure and tourism projects. The icing on the 
cake is the Chinese and Turkey expression of interest to join hands with the GCC 
to set up Free Trade Zones in the region. <br>
<br>
The Summit leaders have gone home from the pulpit. Policy wonks too are back in 
their hunches, tinkering with their rulebook. An open and accommodative mindset 
alone will help them meet the December 2007 deadline of single market, single 
currency and greater prosperity for the entire GCC region.- Ramesh Kumar</font></p>
