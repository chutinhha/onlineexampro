</font><font face="Verdana" size="3"><b>I</b></font><font face="Verdana" size="1">f 
you wish your company to stand out from the crowd in 2006, then you’ll need to 
take a fresh look at your marketing activities and do something different for a 
change. It’s not rocket science and you certainly need not reinvent the wheel. 
In fact, I suggest you borrow some marketing techniques from outside your 
industry. <br>
<br>
Here's an example. Enterprise Rent-A-Car (www.enterprise.com) is one of the 
biggest and most successful rental car companies in the US. Enterprise became 
successful because it catered to a specific niche market - insurance replacement 
vehicles. If you're involved in an accident or your car goes into the garage, 
you need a rental car to drive until yours is ready, and that's where Enterprise 
comes in. <br>
<br>
So what marketing technique did Enterprise use to get noticed? The answer is 
donuts. Enterprise employees regularly visit garages in their area and bring 
free donuts (or pizzas) for the mechanics to eat. It's simple, it's cheap and it 
keeps Enterprise at the top of the list when customers need replacement rental 
cars. Could you use this idea? What would people think of your firm if you were 
giving out free donuts at places where your customers collect? Well, here are 15 
marketing ideas to think about for 2006. <br>
<br>
<b>1. What are you doing? </b>
<img border="0" src="mark1.jpg" width="150" height="167" align="right" hspace="5" vspace="2"><br>
This is the basic stuff. Review the marketing activities you use and the 
marketing skills you and your team have. You’re probably already doing things 
right. But here’s a quick checklist of the bases you should be covering. <br>
<br>
Understand what your market wants; where, when and why your customers buy; what 
benefits they want from your product or service; and be aware of what your 
competitors are offering. Define your objectives, identify and choose your 
target customer groups and position your business to serve your chosen market. 
Monitor the effectiveness of your marketing activities in terms of customer 
satisfaction and the impact on your bottom line. <br>
Just like consumers anywhere in the world, Omani consumers too are becoming more 
demanding and less loyal than ever before. They want to know that your product 
or service will be of value to them and why it’s better than everything else on 
the market from a price, quality or service perspective. In today’s economy, 
you’ve got to be customer-centric; you’d be surprised how few firms are. Make 
customer the focal point of everything your business does. At the end of the 
day, happy customers means greater retu<img border="0" src="mark2.jpg" width="214" height="143" align="right">rns, 
right?<br>
<br>
<b>2. Public Service</b><br>
This is a great chance to do well. Sponsor a blood donation drive. Donate your 
product to local charities or give them out at special events. This is an 
excellent way to position your company in a positive light. The Wisconsin 
Bankers Association's (www.wisbank.com) Personal Economics Program is a case in 
point – it combines financial education and marketing by sending bankers on 
education ventures into schools and colleges. The presentations are particularly 
effective with the parents of children whom the bankers reach at school. It's an 
outstanding example of good old-fashioned &quot;cause marketing&quot; that's even more 
relevant in today’s de-personalized age of low-touch.<br>
<br>
<b>3. Launch a Blog</b><br>
Develop a blog – this is an easy and free way to stand out among your 
competitors, to develop a relationship with current and potential customers. 
Talk about your business. If you run a firm that manufactures mattresses, then 
use your blog to talk about mattresses; give sleep, relaxation and exercise 
tips; special product ideas or ways to use your products; ways your mattresses 
can help alleviate spinal problems. Got a special promotion running? Use your 
blog to announce it. <br>
The same rules apply to blogs as they do to newsletters. If your blog is just a 
sales pitch, don't bother. If, on the other hand, you can provide useful or 
entertaining information for visitors and subscribers, then your firm will 
benefit from a blog.<br>
<br>
<b>4. Not all clients are the same</b><br>
Customers are different so why treat them all the same? Treat them as 
individuals. Look at how and when they buy. This will help to start putting them 
into groups and understanding the needs of each group. For example:<br>
High volume/high value buyers - place frequent large orders <br>
High volume/low value buyers - place frequent small orders <br>
Low volume/high value buyers - place occasional large orders <br>
Low volume/low value buyers - place occasional small orders <br>
Customers are prospects too. Stay in contact with them. Find or develop other 
products or services you can offer them. It's easier to make a sale to a 
previous customer than to someone who’s never bought from you.<br>
<br>
<br>
<b>5. Nag them</b><br>
Never underestimate the nag factor — target Oman’s youth and establishing brand 
loyalty from a young age.<br>
<br>
<b>6. Your environment</b><br>
Pay some attention to how your office looks. Fresh flowers, a clean, light and 
airy office or workspace is welcoming to visiting customers. Indeed, how many 
dirty, badly lit offices have you visited lately? What your office looks and 
smells like says a lot about your company, believe me - so invest in your décor.<br>
<br>
<b>7. Word-of-mouth </b><br>
Word-of-mouth is a cost-effective, powerful form of marketing. Write a list of 
50 people you know but don’t see regularly. Send them a friendly e-mail or 
postcard to let them know what your firm is up to. Ask them for feedback, advice 
or contacts of anyone who might be interested in hearing about your product or 
service. People will help. <br>
<br>
<b>8. Networking For many,</b> <br>
networking can be an intimidating experience but there are ways to make it less 
scary. Networking isn’t about sell, sell, sell - it’s about building 
relationships - go to a networking event looking for opportunities to help 
others. Ask questions, gather information, offer contacts and advice – people 
will remember you for it. Knowledge Oasis Muscat runs a monthly Open House (OER 
supports it!) seminar, it attracts anything from 75 to 150 attendees, this is a 
good place to start.<br>
<br>
<b>9. Business cards </b><br>
Get a new card designed, stand out from the crowd. For example, print it on 
unusual material –
<img border="0" src="mark3.jpg" width="150" height="112" align="right" hspace="5" vspace="2">textured 
card, plastic, wood – or make it an unusual shape. Offer a business tip or 
special offer on the back. <br>
<br>
<b>10. Testimonials </b><br>
Collect testimonials from your customers and use them in your advertising. 
Testimonials provide evidence that your product or service delivers the results 
you promise. For maximum impact, use only testimonials that describe specific 
results the customer enjoyed. After all, testimonials also offer your customers 
great publicity. It’s win-win.<br>
<br>
<b>11. Press releases </b><br>
They have to be targeted. There is little point sending round a generic press 
release to lots of newspapers and business magazines. Start by focusing on five 
publications ideal for your target market. Read back editions thoroughly – 
understand what kind of stories they like to publish and the style of language 
they use. Find a relevant news hook and tailor your press release specifically 
for each publication. The first paragraph is key and must provide the who, what, 
where, when and why of your story. When Jim Dutchess started a mobile wash 
business, he called the wire service UPI, told them he had a truck with portable 
washing equipment, and made an offer to go anywhere in the US and wash a UFO for 
free if he could get there in time. After the reporter stopped laughing, he ran 
the story. Dutchess got national coverage, became a celebrity in his home market 
and saw his start-up business boom.<br>
<b><br>
12. Image: Look beyond price</b><br>
Move away from price. Promote the value of your product or service not simply 
its price. Your customers and potential customers are looking for value and they 
believe you can provide it. Your challenge is to promote and sell the value of 
your product or service and ask your customers to take action to receive that 
value.<br>
<br>
<b>13. Postcards
<img border="0" src="mark4.jpg" width="150" height="131" align="right" hspace="5" vspace="2"></b><br>
Print your best small ad on a postcard and mail it to prospects in your targeted 
market. Postcards are inexpensive and easy to use. Most recipients who ignore 
other types of advertising will read a brief ad when it's delivered to them on a 
postcard. Corporate Identity Solutions (www.identify-yourself.com) created and 
mailed a set of four postcards celebrating spring and featuring beautiful 
flowers and their symbolic meanings. A fifth card served as an introduction to 
the set, bearing the company's wishes for a happy spring season. It also 
explained the significance of the selected flowers (respect, wisdom, 
distinction, devotion) and how each relates to the company's approach. The 
reverse side of the postcards was left blank for the recipient to mail to a 
friend or associate. At a little more than a 500 baizas a pack, including 
twine-tying and packaging, this direct mail effort delivered viral impact with a 
deft, light-handed approach.<br>
<br>
<b>14. 80–20 Rule</b><br>
Continually test and evaluate everything you use or do to market your business. 
Allocate 80 per cent of your advertising budget to proven promotions. Use the 
other 20 per cent for testing new variations<b><img border="0" src="mark5.jpg" width="150" height="178" align="right" hspace="5" vspace="2"></b>. 
Most businesses using this system continue growing - even in highly competitive 
markets.<br>
<br>
<b>15. Power of Odd Numbers</b><br>
Express numerical claims as odd numbers with fractions or decimals. For example, 
&quot;Our clients save 18.3 per cent&quot; sounds more credible than &quot;Our clients save 20 
per cent&quot; ...even if 20 per cent is the accurate number. <br>
<br>
Develop a series of four or five different special offers. Use them one at a 
time with an expiry date. When one offer expires, replace it with the next offer 
and a new deadline. Continuously recycle through the same series of offers. <br>
This enables you to keep using special offers to generate sales without taking 
time to develop new ones. Remember, the majority of sales aren’t made on the 
first contact.Develop a method to capture and save the names and contact 
information of prospects who don't buy from you. Follow up periodically. A 
little gentle coaching will eventually convert many of them into buyers.<br>
Each of these 15 marketing ideas can be highly effective. Take action now to 
apply those you overlooked. You'll be surprised to see how much business it 
brings in. Here’s to a successful 2006! - Matt Harrison</font></p>

