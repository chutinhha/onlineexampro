            <p align="justify">
            <font face="Verdana" size="3">
            <b>O</b></font><font face="Verdana" size="1">man is one country in 
            the region where the awareness of preserving nature and conservation 
            of resources is the highest. The vast amount of historical data 
            available on rainfall, ground water levels and quality etc 
            substantiate this. In fact, Oman is the first country in the region 
            to have proper regulations put in place to prevent ground water 
            exploitation. <br>
            <br>
            The initiatives in this direction will certainly pave way for 
            augmenting people’s awareness towards protecting the environment and 
            conserving natural resources.<br>
            <br>
            When we talk about our expectations for the country for the coming 
            year, according to me, energy and water conservation, green 
            buildings and clean development mechanism will be the thrust areas 
            for Oman from an environmental point of view. By adapting to these, 
            the country not only protects the environment, but also saves large 
            amount of money, which could be utilised for other developmental 
            activities.<br>
            <br>
            Scarcity of ground water is a major problem being faced by the 
            region. For that reason, measures to utilise the most modern 
            technologies to conserve water shall be looked upon as priority in 
            the New Year. The outlook of considering effluents as mere waste 
            should change and effluents should be looked at as misplaced 
            resources that could be utilized well. <br>
            <br>
            Re-use of water is not very well established in this region. 
            Traditionally industrial effluents are treated to meet some basic 
            standards essentially to minimise the impact on the environment. But 
            advanced technologies available today shall be employed to re-use 
            them in the process. This will be another thrust area for Oman in 
            the immediate future.<br>
            Sewage treatment has been in vogue the world over for centuries with 
            a view to minimising the damage which untreated sewage can cause to 
            ground and surface water, the marine environment and the very soil 
            in which we cultivate. The modern technologies make it possible to 
            treat sewage to much higher standards so that they can be used for 
            various applications such as construction, recharging aquifers, 
            toilet flushing, cooling towers, car wash etc. This concept should 
            figure when we talk about the plans for the future. <br>
            <br>
            The concept of Grey Water Recycling is yet to gain popularity in 
            Oman. But this is an area, which should not go unnoticed. Grey water 
            treatment and re-use would save the country millions of Rials every 
            year as water is subsidised by the government. It can be put as a 
            regulation for all new buildings to have separate lines for grey 
            water and black water which makes it possible to collect, treat and 
            re-use grey water.<br>
            <br>
            Capacity enhancement of existing plants is something that needs to 
            be looked into rather than going for new plants to cater to the 
            increased loads on existing plants. Wherever new plants are required 
            to be installed, very compact plants working on modern technologies 
            should be employed rather than following the traditional treatment 
            methods. This would save a lot space and thereby, a lot of money, in 
            terms of lesser usage of the precious real estate.<br>
            <br>
            Desalination is an imperative evil the region has to live with 
            because of the sparse availability of ground and surface water for 
            potable purposes. Desalination is energy intensive and so naturally 
            damaging to the environment. The damage is further aggravated by the 
            reject water from desalination plants, which increases the salt 
            level in the seawater or ground water, depending on where it is 
            discharged. It is in this context that highly energy efficient 
            technologies should be utilised for desalination rather than 
            sticking on to the traditional single pass reverse osmosis systems.<br>
            <br>
            Reducing consumption of water in houses, hotels and other 
            residential places is another area to be looked at. Very 
            economically priced fittings are available for conserving water in 
            showers, faucets and toilets, which do not have any adverse impact 
            on the comfort levels but save a lot of water. These fittings would 
            pay for themselves in a matter of days. People should be encouraged 
            to use these as while they can save money, the government saves on 
            subsidy and the nation as a whole preserves its environment and 
            resources -- a win-win situation for all. It is pleasant to note 
            that some of the hotels in Oman are already using these fittings.
            <br>
            <br>
            Power is another utility service heavily subsidised by government. 
            The more power generation, the more pollution and faster the 
            depletion of resources. There are modern technologies available for 
            energy conservation. Municipalities, hotels, commercial 
            establishments and large industries can do a lot towards achieving 
            this goal by employing these technologies.<br>
            <br>
            Sustainable development is a much talked-about subject. The concept 
            of Green Buildings is something to get deep rooted in the society. 
            Initiatives in that direction will be another useful thought for the 
            future. Green buildings are environment-friendly, occupant-friendly, 
            and resource-efficient. <br>
            <br>
            Green building methods can make existing buildings three to four 
            times more resource-efficient and new buildings ten times more 
            resource-efficient.<br>
            <br>
            The point to ponder is: “We did not inherit this air, water and soil 
            from our ancestors; we owe it to our future generations. The modern 
            world shall not sustain any growth or development ignoring this 
            fact.”</font></p>
