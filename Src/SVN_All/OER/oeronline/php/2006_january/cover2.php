            <p align="justify"><font face="Verdana" size="3"><b>Y</b></font><font face="Verdana" size="1">ear 2005 was very meaningful for the relations between the Republic 
            of Korea and the Sultanate of Oman, as two very high-level 
            delegations from both countries made official visits to each other’s 
            capitals in a span of just two months. First, HH Sayyid Fahd, Deputy 
            Prime Minister for the Council of Ministers of Oman, visited Korea 
            in late September, and then, HE LEE Hae-chan, the Korean Prime 
            Minsiter, visited the Sultanate in November-end. <br>
            <br>
            Such mutual visits of high-level delegations in such a short period 
            - which is not so frequent even for the neighbouring countries - 
            clearly shows that the Korea-Oman ties are very deep-rooted and that 
            both countries are fervently seeking to explore new avenues of 
            cooperation.<br>
            <br>
            Since I came to Oman as Ambassador in March 2005, I have witnessed a 
            remarkable expansion of exchanges and cooperation between the two 
            countries. Statistics also bear testimony to growing Korea-Oman 
            trade ties. In the year 2004, Korea’s exports to Oman stood at $157 
            million whereas that of 2005 (till October) stands at $213 million, 
            an increase of 76.6 per cent. Also, the imports from Oman to Korea 
            in 2004 were valued at $2.5 billion, whereas the same in 2005 (till 
            October) stands at $2.9 billion, which is an increase of 44.8 per 
            cent. Besides the impressive increase in trade volume, the number of 
            visitors to each other’s country has registered a remarkable 
            increase, going 44 per cent up in 2005 as compared to that of 2004.<br>
            <br>
            My forecast on the relations between the two countries in 2006 is 
            that the trend of expansion of cooperation and exchanges, which the 
            two countries have seen in 2005, will be further accelerated, 
            particularly, cooperation in the economic field, which will be 
            carried forward in more diversified ways and areas. This is also 
            because the “Committee for Economic Cooperation”, headed by the 
            Undersecretaries of the two countries – which was discussed during 
            the high-level delegation visits in 2005 -- will be set up and 
            formally start its work in 2006. <br>
            <br>
            <img border="0" src="cover3a.jpg" width="200" height="167" align="right" hspace="5">The IT industry, in which Korea enjoys high competitiveness 
            globally, will be one of the major areas for close cooperation 
            between the two countries. The defence industry, the quality of 
            which is decided by the standard of the IT technology, is another 
            potential sector of cooperation. In the area of direct investment, I 
            am very hopeful that the mutual investments from each country will 
            experience an upward trend in the coming months.<br>
            <br>
            In particular, I expect that Korean companies, with their expertise 
            in manufacturing daily consumer products, would invest in setting up 
            of small and medium size factories in the downstream petrochemical 
            industry in the Sultanate. This sort of direct investment by the 
            Korean companies will not only contribute to the creation of more 
            jobs for Omanis, but will also be beneficial to themselves, as the 
            Sultanate, with its strategic geographical location, providing a 
            gateway between the Indian Ocean, East Africa and the Arabian Gulf, 
            could act as a bridgehead for exports to these regions. In addition, 
            I expect more Korean companies to participate in the Sultanate’s 
            ambitious economic development plan.<br>
            <br>
            The other areas of cooperation, which will be further explored in 
            the year 2006, are sports, tourism and culture. I am confident that 
            with a variety of exchange and cooperation programmes in these 
            fields, both Koreans and Omanis can deepen their mutual 
            understanding and consolidate their friendship for years to come.</font></p>
