<p align="justify" dir="ltr"><font face="Verdana" size="1">Qatar is
<img border="0" src="qatar_pic.jpg" align="right" width="150" height="147">ending 
bank brokerage from March 1, 2006 in phases. The Doha Securities Market (DSM) 
has terminated the licences of local banks offering brokerage services and these 
operations will merge into one superbroker, Al Dilalah. The banks will not 
accept new accounts from January 1. However, licensed brokerage banks will 
conduct brokerage business for the existing clients only for the month of 
