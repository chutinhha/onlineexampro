<p align="justify" dir="ltr"><font face="Verdana" size="4" color="#FF0000"><b>
Challenging times<br>
</b></font><font face="Verdana" size="1"><br>
It is no longer just a grand sporting event, but an anthem every resident wakes 
up to. For the much-awaited Doha Asian Games 2006, the official countdown<img border="1" src="challenging_times.jpg" align="right" width="200" height="90"> 
had begun a year ago, on December 31, 2004. <br>
<br>
The West Asian Games (December 1-10, 2005) was the trial run not only for the 
athletes but for the organisers as well. It was the first big multi-sport event 
Doha hosted, and was the last chance the country could afford to make mistakes 
and rectify them. The 3rd West Asian Games got off to a colourful start at the 
Qatar Sports Club, which for the first time, allowed women to compete.<br>
<br>
The Games were contested by more than 1,400 athletes from 13 participating 
countries in 12 disciplines – athletics, bowling, basketball, diving, fencing, 
football, gymnastics, handball, swimming, shooting, volleyball and 
weightlifting. A total of 12 venues were used to conduct some 115 events. The 
10-day Games were held under the patronage of the Heir Apparent HH Sheikh Tamim 
bin Hamad Al Thani, who is also the president of the QNOC and chairman of the 
Doha Asian Games Organising Committee (DAGOC). <br>
<br>
History was made at the West Asian Games as women from five countries in the 
region competed in the swimming competition for the first time. The 17 women 
from Lebanon, Palestine, Qatar, Syria and Jordan competed in 10 events. West 
Asian Games brought to light the various challenges Doha faces in making 2006 
Games a success. After all, it will require a zero-tolerance approach. Hiccups 
faced in WAG will become an international fiasco if repeated in 2006. Here is 
one: The Syrian football team lost their way to match venue because of jumbled 
up travel plans. They missed their match against Oman after arriving at the 
wrong venue (Al Wakrah Stadium) instead of appearing at Al Gharrafah Stadium.<br>
<br>
The joke going around was that if the confused driver had been a Lebanese (he 
was an Asian), then this would have become a diplomatic ordeal for the country. 
Anyway, the facilities tested were just a fraction of what will be used next 
year, when tens of thousands of athletes from 45 countries participate in 39 
sports. Over 14 multi-purpose stadiums are being refurbished to host the events.<br>
<br>
Most importantly, Doha will have to bring together 15,000 volunteers to ensure 
the success of the Games. Unlike most other venues, a bulk of these volunteers 
will be expatriates. Doha won the rights to host the Games, beating strong 
contenders like Kuala Lumpur. And now, it will be closely scrutinised. Doha 
should remember that while every success will be applauded, every slip could 
well be magnified.<br>
Vani Saraswati, Qatar</font></p>

