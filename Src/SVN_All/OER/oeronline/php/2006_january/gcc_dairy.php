<p align="justify" dir="ltr"><font face="Verdana" size="1"><br>
<img border="1" src="bahrain.jpg" align="left" width="250" height="42">Bahrain 
became the first Gulf country and the third Arab country to have a Free Trade 
Agreement (FTA) with the United States after the Senate gave the green signal to 
the agreement. Negotiations for the FTA began in January 2004 and the Agreement 
was signed in September that year. On November 18, 2005, the FTA was approved 
unanimously by the House and Senate Committees. In the full House of 
Representatives, the vote was 327-95 in favor of the FTA. Senate’s consent was 
the last hurdle in the legislative process. Upon US President George W. Bush’s 
signature, the FTA will become official.<br>
<br>
Two-way trade between these countries was nearly US $900million in 2003. US 
exports to Bahrain in 2003 totaled $509 million, including aircraft, machinery, 
vehicles, pharmaceutical products, toys, games and sports equipment. US exports 
of agricultural products to Bahrain in 2003 totaled $14 million, including 
poultry, snack foods, cotton, and processed fruit and vegetables. Bahrain’s 
exports to the US in 2003 were valued at $378 million, including articles of 
apparel and clothing accessories, aluminum, fertilizers, organic chemicals, 
mineral fuels and oils, plastics, and electrical machinery. The two-way foreign 
direct investment stands at about $195 million. When the agreement comes into 
force, 100 per cent of consumer and industrial products and 81 per cent of US 
agricultural exports will be duty free. Bahrain will open its services market 
wider than any previous FTA partner, streamline digital trade, protect 
intellectual property, facilitate government procurement, and provide for 
effective enforcement of labour and environmental laws.</font></p>

