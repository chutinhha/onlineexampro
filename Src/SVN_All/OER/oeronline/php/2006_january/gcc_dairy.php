<font color="#FF0000"><b>
<img border="0" src="uae.jpg" align="left" width="150" height="52"></b></font>According 
to a survey carried out by the Dubai Chamber of Commerce and Industry, local 
insurance companies have expressed fears of domination by foreign insurance 
companies. Though many of the local companies expressed confidence that free 
trade agreements (FTAs) which the UAE is currently negotiating with many 
countries will help increase their business. Apprehensions are raised about “the 
invasion of Multi National Companies (MNCs)”. Overall, the industry is 
optimistic that the FTAs will increase competition and improve the services 
provided by the sector. In addition, they said it would open new areas and 
opportunities to improve business and decrease risks, extend the scope of 
e-commerce and develop new ways of marketing channels. <br>
<br>
The extent to which the lower-end popular insurance activities will provide 
opportunities to the foreign insurance companies will depend on the activities 
of the insurance companies from FTA countries and their degree of specialization 
in the respective activity. The survey showed that the most popular and common 
activities that are provided by all companies are automobile, fire, cargo and 
fixed property insurance. According to the survey, Dubai is considered to be the 
hub of insurance industry in the UAE. It hosts 49.4 per cent of the total 
establishments, followed by Abu Dhabi with 32.9 per cent and Sharjah with 10.4 
per cent. About 93 per cent of the operating establishments are hosted by the 
biggest three emirates.</font></p>

