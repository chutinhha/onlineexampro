<p align="justify" dir="ltr"><font face="Verdana" size="1"><b>
<img border="1" src="saudi_arabia.jpg" align="left" width="212" height="64"></b>The 
Gulf will never be the same again. In December 2005, the Custodian of the Two 
Holy Mosques King Abdullah launched the US$26.6-billion mega economic city 
project - christened The King Abdullah Economic City. The largest-ever joint 
venture in the Kingdom involving Saudi Arabia and the United Arab Emirates is to 
be built at a pristine location off the Red Sea.<br>
<br>
The new age city will have six distinct components: a modern world-class 
seaport, an industrial district, a financial island, an education zone, resorts 
and a residential area. The project will be completed in stages with the first 
batch of businesses and residents moving into the city within two years. Emaar 
Properties, the world’s largest real estate company in terms of market 
capitalization, will be the master developer of this ambitious project, the 
biggest outside of its home market of the UAE. SAGIA, the apex body responsible 
for inward investments into the Kingdom, is the prime facilitator for the 
development.<br>
<br>
SAGIA’s Investor Service Center will facilitate the provision of services to 
potential investors. Moreover, companies and investors would get licenses for 
implementing their projects in the city within a week after presenting 
applications. Central to the mega project is the creation of a 2.6 million 
square meter new Millennium Seaport similar in size to the world’s top 10 ports. 
With its strategic location on the Red Sea and the instant access to key cities 
within the Kingdom, the port will have a designated area for light industry and 
logistics and will be a natural platform for onward movement of goods to Europe, 
Africa, Asia and beyond. <br>
<br>
The Industrial District, covering 8 million square meters, will represent 
sectors such as downstream petrochemicals, pharmaceuticals, research and 
development activities as well as a host of educational institutions. The 
waterside resort will serve up a most compelling mix of waterfront hotels and 
boutique residences. <br>
<br>
The master plan envisages 3,500 well-appointed hotel and residential bedrooms 
and suites, premium villas, plus an extensive retail element and an 
international-class signature 18-hole golf course and an equestrian club.<br>
<br>
The Financial Island will offer 500,000 square meters of office space for the 
leading international and regional financial services providers, business hotels 
and a new exhibition and convention center. There will be two towers reaching up 
to 160 stories that offer compelling views of the surrounding city skyline.</font></p>

