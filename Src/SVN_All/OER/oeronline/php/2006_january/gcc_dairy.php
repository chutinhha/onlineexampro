<font color="#FF0000"><b>
<img border="1" src="kuwait.jpg" align="left" width="285" height="61"></b></font>Kuwait's 
parliament has set January 23 date to vote on Project Kuwait. If it agrees to 
allow foreign oil companies to exploit Kuwait's oilfields again, Saudi Arabia 
will be isolated as the region's only country to bar international oil companies 
from its fields.<br>
<br>
Project Kuwait is a $7 billion plan to boost the production and reserves of 
Kuwait's technically challenging northern oil fields with the help of 
international oil companies. World's largest energy groups, including Britain's 
BP and ExxonMobil and Chevron of the US are already showing interest. Kuwait has 
the world's fifth largest oil reserves. It needs to boost output to 4 million 
barrels a day to meet the growing world demand, according to the International 
Energy Agency.</font></p>

