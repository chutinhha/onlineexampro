</font><font face="Verdana" size="4" color="#FF0000">
<br>
</font><font color="#FF0000" size="4" face="Verdana"><b>Connectivity on ‘track’</b></font><font face="Verdana" size="1"><br>
<br>
<img border="0" src="connectivity_track.jpg" align="left" width="150" height="129">Policy-makers 
in the Middle East realise the importance of a competent and swift transport 
system. In the next 5-7 years, the Gulf countries are expected to invest around 
RO3 billion on rail projects, according to industry estimates. In mid-December, 
leaders from the Gulf region came together under the banner of Gulf Traffic 2005 
Exhibition to brainstorm on the most talked about subject in the region.<br>
<br>
Some of the prestigious projects that are under various stages of implementation 
include Emirates Railway (UAE’s National Railway Network), Dubai Metro Rail, 
Riyadh Light Railway Project (Saudi Arabia), GCC Rail Network (linking all six 
Gulf states) and Arabian Railway Network.<br>
<br>
The proposed Emirates Railways would cost between RO1 billion and RO1.43 
billion, covering 700-1000kms, connecting Ruwais Industrial Area in Abu Dhabi to 
Ras Al Khaimah’s Port Saqr, Jebel Ali Port, Jebel Ali Airport City, Dubai city, 
Sharjah, Furairah port and Khorfakkan. In the first phase, only cargo 
transportation will be handled. In the second phase, the facilities will be 
extended to passengers as well. <br>
<br>
German Transport consultants Doriner is carrying out a feasibility study for the 
project and is expected to submit its report by September this year. According 
to its pre-feasibility study, the network can handle 16.5 million tons of cargo 
between Dubai and Abu Dhabi. If this network really comes into force, traffic 
along the inter-emirates network will be eased. The rail line is planned in such 
a way that it could be integrated with the GCC rail network in future.<br>
<br>
Meanwhile, construction for the ambitious Dubai Metro project, also known as 
Dubai Light Rail Project, has finally started. A two line (Red and Green) fully 
automated and driverless system will run through a length of 69.7kms. 
Construction of phase one will finish in June 2009 and the construction of the 
second phase will start in 2007, to be completed by 2010. <br>
<br>
The Dubai Municipality awarded the RO1.24 billion contract to the Dubai Rapid 
Link (DURL) consortium headed by Mitsubishi Corp of Japan. The Red Line, which 
will be constructed in phase one, will run from Rashidiya Station to Jebel Ali 
Station. It will be 52.1km long with 4.7km of underground and a 47.4km elevated 
line. The Green Line, which will be constructed during phase two, will be 17.6km 
long with a 7.6km underground line in the business district of Dubai. It will 
run from Dubai Airport Free Zone to Dubai Healthcare City. A total 87 trains 
with five carriages each will run on these two lines. The fully operational 
system is expected to carry about 1.85 million passengers each working day with 
an annual total of 650 million passengers.<br>
<br>
Saudi Arabia also made considerable advancement in its railway expansion 
projects. The Riyadh Light Railway Project, linking the east and west coasts of 
Kingdom as well as the cities of Makkah, Madinah and Jeddah, has been making a 
remarkable headway. The project worth RO400 million will be open to private 
participation. The project involves construction of new tracks between Riyadh 
and Jeddah, and another 115-km line between Dammam and Jubail as well as 
upgrading the existing rail link between Riyadh and Dammam.<br>
<br>
Cargo trains will be able to cover the distance between Jeddah and Dammam within 
24 hours. The new railway project, once completed, will promote travel and 
freight transportation across the Gulf.<br>
<br>
GCC Linkage: Increasing trade and tourism has made governments across Gulf 
ponder over inter-transport connectivity. This thought has given birth to two 
significant projects – Arab Rail Network and GCC Rail Network. <br>
<br>
In July 2005, the GCC requested Saudi Arabia to carry out a technical study on 
the establishment of a GCC rail network. According to media reports, an initial 
study proposes construction of two lines, the first being 1,970km long and 
stretching from Kuwait to Saudi Arabia, Bahrain and through a bridge to Qatar, 
and from there to the UAE and Oman. The second line would be 1,984km long 
running from Kuwait to Saudi Arabia to the UAE and Oman. Connecting points will 
be in Bahrain and Qatar. The cost was estimated to be RO2.2 billion.<br>
<br>
On the other hand, the Arab Rail Network will stretch from the Syrian/Turkish 
border in the north to Aden in the south-west through Arabic-speaking North 
Africa to Mauritania on the Atlantic coast and as far east as the Iraqi border 
with Iran. North Africa, Egypt, Sudan, Tunisia, Algeria, Morocco and Mauritania 
- all have existing railway lines, which would be utilised in the network 
covering over 25,000kms, said Murhaf Al Sabouni, General Secretary of the 
Syria-based Arab Railway Union.<br>
<br>
The roadmap for the Arab rail network is yet to be prepared. However, the Arab 
Railway Union proposed to connect the seven emirates of the UAE, considering its 
growing trade and tourism activities. <br>
<br>
Overall, massive investments across the region in future will depend on the 
effective mode of transportation of that country and its connectivity within the 
region. <br>
- Sowmya Sri Rallabhandi</font></p>

