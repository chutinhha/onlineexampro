making gradual inroads in Oman. Our products are modern though we have a few 
traditional pieces. We cannot introduce ultra-modern products here, as the 
market is not yet ready to accept it. We do try to push the boundaries a little 
bit more every five to six months by getting modern pieces of furniture and 
products. At times we do bring in some extremely modern stuff to assess the 
market too.<br>
<br>
