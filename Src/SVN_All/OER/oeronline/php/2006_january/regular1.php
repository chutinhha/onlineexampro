<b>Bringing in changes </b><br>
My predecessor, who set up the store, did a great job with products that were 
niche and top of the range. He had styled the store in a very Danish fashion, 
with North European leanings. The furniture was very light in colour — beige, 
camel brown and sand brown. It is an easy sell among the Western Europeans but 
local market penetration was not taking place. So I added more colours and also 
changed some models of the lifestyle products and furniture we had. We also 
increased the products in the home accessories section. We don’t want to be just 
another shop selling household items. I’d rather have someone walk into the 
store and either fall in love or faint in horror!<br>
