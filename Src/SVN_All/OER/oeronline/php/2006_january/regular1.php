<b>Management with a difference </b><br>
As the General Manager of Oasis Lifestyle LLC (which has under its umbrella the 
brands Zone, ID Design, Shop &amp; Shop, Bodum and Bombay), my responsibilities 
include just about everything from buying, pricing, advertising, marketing, 
human resources, operational management, and recruitment to budgeting. My team 
ably supports me in my work. I like to get the team involved in all the work I 
do. I try to encourage the staff to do as much as they can. I feel people should 
be allowed to do more than what has been assigned to them. I think you should 
give people as much free rein as possible. <br>
<br>
Many a time, those at the top take it as threat to their positions to let anyone 
in the team to do this. But I feel that getting people involved more is only a 
benefit to the company. That’s what all the top companies follow. <br>
<br>
I do not believe in following any particular management style because I don’t 
think there’s any absolute management principle. If there were one, then 
