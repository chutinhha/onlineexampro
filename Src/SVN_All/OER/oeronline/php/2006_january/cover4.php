            <p align="justify"><font face="Verdana" size="3">
            <b>T</b></font><font face="Verdana" size="1">he year 2006 may very 
            well be considered as the year of tourism. One should not be 
            surprised if initiatives relating to tourism overshadow that of mega 
            industrial projects. It is expected that Oman would launch an 
            awareness campaign on Oman’s potential as a tourist destination, 
            coupled with a liberalisation in issuing visas to tourists. The new 
            hotel and real estate projects spearheaded by the Blue City and Wave 
            Projects should make the world sit up and take notice of Oman.<br>
            <br>
            <b>Economy</b><br>
            The year 2006, as I can see, will be a year of consolidation, in 
            which the next stage of the development of the Sultanate will 
            unfold. We should expect that the government would continue its 
            prudent and conservative fiscal and economic policies in the year 
            2006. The budgeted oil price for the Seventh Five-year Plan - US$ 30 
            per barrel - is conservative, given the medium term forecasts by 
            various analysts. <br>
            A conservative estimation of the oil price is likely to result in 
            surpluses that would be utilised to strengthen the financial 
            reserves of the country and directed towards building the country’s 
            diversification initiatives. <br>
            The banking and capital markets regulators will continue to govern 
            with caution as witnessed in the past. The year 2006 may very well 
            see a number of initial public offerings adding more depth to the 
            market. With strong fundamentals and conservative government 
            policies, the Muscat Securities Market index should go up during the 
            year 2006.<br>
            <br>
            <b>Human resources</b><br>
            In conjunction with the launching of the Seventh Five-year Plan, a 
            great deal of attention will be paid in 2006 to human resource 
            development. We should expect the focus to be directed towards 
            offering training to nationals, which is relevant to the needs of 
            the economy. The thrust will be on vocational training and higher 
            education. There is growing evidence that Omanisation will result in 
            the creation of jobs in which Omani nationals could look forward to 
            career development. The emphasis will shift from quantity to 
            quality.<br>
            <br>
            <b>Diversification</b><br>
            The initiatives taken in the last few years for the diversification 
            of the economy will start to show results in the 2006. The benefits 
            of a full year’s production and exports from both the third train of 
            Oman LNG as well as from the fertilizer project in Sur should accrue 
            to the country in 2006. The petrochemical, aluminium smelter, 
            polypropylene, aromatics, fertilizer and other projects in the Sohar 
            area will propel the local economy to greater heights. <br>
            <br>
            <img border="1" src="cover5a.jpg" width="120" height="80" align="right" hspace="5" vspace="2">The 
            year 2006 should also see announcements in a number of downstream 
            projects, which will be a logical extension to the current mega 
            industrial projects under progress. The emergence of Oman Shipping 
            Company, as an important component of Oman’s economy, will be a 
            striking feature of the year 2006. We may also expect a number of 
            projects involving the Salalah, Sohar and Duqm ports. 2006 will also 
            see an acceleration of development activities in connection with the 
            proposed free trade zone.<br>
            <br>
            <b>Privatisation</b><br>
            Oman has been a pioneer in the Gulf region in privatization. The 
            coming year should see a stream of privatization-related activities 
            in the electricity, water, telecom and postal sectors. The 
            government may carry out a comprehensive review of other areas for 
            privatisation with a view to keeping the momentum going.<br>
            <br>
            <b>Laws</b><br>
            Year 2006 should also mark the introduction of new foreign 
            investment, commercial companies and income tax laws. Simplified 
            measures to attract multinational companies to set up 100 per cent 
            owned subsidiaries in Oman may become a reality in 2006. <br>
            <br>
            Concurrently, the 24-year-old tax laws may be substituted with a new 
            law that addresses the current realities. A new commercial companies 
            law that incorporates a higher level of corporate governance and 
            minority shareholder protection may also be launched. While personal 
            tax is unlikely to be introduced, the country may move closer 
            towards widening its tax base by looking closely at the introduction 
            of value added tax on a selective basis.<br>
            Overall, the year 2006 promises to be interesting and will reinforce 
            the current high level of optimism shown by the country’s private 
            sector. The Omani citizens and residents can look forward to a 
            dynamic and prosperous 2006.<br>
