Compared to banks, credit quality of NBFCs is higher due to the asset based 
lending business model. The credit risk of UFC is narrowed further due to its 
huge customer base (12,000 clients, average loan per customer of RO8,000) 
coupled with loans secured against assets. In a high growth, high liquidity 
economy, collections will be high while loan default will be low. However, new 
loan additions at lower interest rates will be eligible for higher loan loss 
provisions, marginally offsetting the significant growth in interest income.<br>
<img border="0" src="market_view2jpg.jpg" width="300" height="152" align="right" hspace="8" vspace="6"><br>
We expect the company to report 35 per cent growth in its receivables portfolio 
and a very marginal increase in the spread for UFC in FY08. Currently, the stock 
is trading at 10 times FY08 earnings estimate and 1.7 times FY07 book value. UFC 
stands to benefit from the increasing growth across various sectors and funding 
opportunity backed by high liquidity in the region. We believe UFC is well 
positioned to outperform the sector and rate the stock a BUY with a target price 
of RO0.269/share <br>
<br>
&nbsp;</font><p align="justify" style="margin: 3px 10px"><font size="1" face="Verdana">OIFC’s contract with Oman Mobile to market Hayyak starter kits and prepaid card 
enhances its revenue streams. The market for prepaid cards is expected to be 
around RO5.5 million per month. Apart from OIFC, there are three more players in 
the Oman Mobile prepaid card marketing business. A single competitor owns 
approximately 60 per cent of the market share while the other three, including 
OIFC, are new entrants. OIFC expects to sell approximately RO1.5 million worth 
of cards per month, utilising its existing infrastructure, with virtually no 
additional costs. Hence, the revenue generated from this business is expected to 
add to the bottomline. Though the business is lucrative, but given the 
unorganised nature of the market; OIFC faces intense discount rate competition. 
Hence, OIFC is targeting the organised retail sector, including retail chains 
and smaller shopping malls, by extending credit days. The business also 
generates positive free cash flow for the company.</font><img border="0" src="market_view3.jpg" width="449" height="337" hspace="8" vspace="6"><p align="justify" style="margin: 3px 10px"><font size="1" face="Verdana">
<br>
OIFC has been able to expand its operating margins from 21 per cent in FY05 to 
33 per cent in the first nine months of FY08. The company’s growth in billing 
from Omantel and MHEW has been a major driver in this. Ongoing infrastructure 
development projects, economic growth and increased population in the Muscat 
region are all indicative of a further rise in consumption of water and 
electricity. Water distribution was badly affected during cyclone period in June 
and July 2007; hence, there was a slight drop in distribution of water in 2007 
compared to 2006. However, monthly figures show a significant uptrend.<br>
<br>
Trading at a 15 per cent discount to our target price of 688 Bz, and with 
approximately 5 per cent (expected) dividend yield, the shares of OIFC seems to 
be a good investment opportunity. We believe the company has the wherewithal to 
add more clients to further leverage its reach and strategic position in the 
debt factoring business. Our estimates may prove to be conservative, if the 
company opens any fresh channels or adds new clientele.
