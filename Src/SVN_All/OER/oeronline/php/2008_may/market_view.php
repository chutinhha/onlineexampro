</font><font size="4" face="Verdana"><b>Volumes speak</b></font><font size="1" face="Verdana"><br>
<b>The month ending April 15 was mixed for GCC bourses, but the average daily 
turnover on the MSM30 indicates renewed interest<br>
</b><br>
The MSM30 Index performance was muted for the period March 16 to April 15, as 
the market waited for 1Q FY08 results. MSM30 index gained a tad 2 per cent 
during the period but with encouraging volumes. The fact that the average daily 
turnover has bourgeoned from an average of RO10 million in 2007 to RO19 million 
for the same period, indicates the growing participation of foreign and retail 
players, and the noteworthy performance of Oman based companies.<br>
<br>
The industry sector index outperformed the other sectoral indices during the 
period. Heavy weights in the industry index like Oman Cement (+14 per cent), 
Raysut Cement (+11.8 per cent) and Jazeera Steel products (+10 per cent) helped 
push the index north. However, the performance of services sector index 
heavyweights like Omantel (-9 per cent), Shell Oman (-9 per cent) and United 
Power (-12.8 per cent) was a drag on the overall index. Below par performance in 
few stocks was partially reflecting cash dividend adjustments.<br>
<br>
In corporate events, Bank Sohar decided to call the balance 50 per cent of the 
capital within the second quarter of 2008 and split the nominal value of the 
share from RO1 to RO0.100. In other major events, Dhofar Cattle Feed (DCF) has 
been allowed 25 per cent of Graziz Farm for the next 50 years while the Sahnout 
Farm lease agreement was renewed for the next 10 years with existing farm 
utilisations. DCF was also allowed participation in establishing a new farm at 
Naged for grass cultivation. National Aluminum Products Company (NAPCO) board 
approved the expansion of the existing installed capacity by 800 tonnes per 
month, with an expected cost of RO4.1 million.<br>
<br>
In result announcements, the start to first quarter results was encouraging with 
Oman Cables, Oman Chlorine and Al Jazeera steel products reporting commendable 
growth. Oman Chlorine reported net profit growth of 6.6 per cent YoY to 
RO648,000 while Al Jazeera Steel Product’s net profit increased 224 per cent QoQ 
to RO1.5 million.<br>
<br>
GCC Market Commentary<br>
The month ending April 15, witnessed a mixed performance on the GCC bourses, 
with the Doha Stock Exchange registering a rise of 6.08 per cent, the highest 
among the GCC markets. The industrial and banking indices were among the ace 
gainers on the exchange, gaining 8.81 per cent and 6.14 per cent, respectively, 
as compared to last month. Kuwait and Abu Dhabi markets too ended in the green 
and closed up 2.92 per cent and 2.55 per cent respectively, during the month.<br>
<br>
While industrial counters were among the top gainers on the Kuwait bourse, 
improving by 10.53 per cent, the insurance index topped the Abu Dhabi exchange 
by rising 7.12 per cent. The Bahraini bourse ended down 0.52 per cent on the 
back of selling seen primarily in the insurance and industrial counters. The 
Saudi market, which was the highest YTD loser among the GCC bourses, shed 1.78 
per cent during the month, with all major indices ending in the red. The Dubai 
Financial Markets were among the worst performers during the month and the 
general index lost 4.88 per cent. The banking and the investment counters were 
among the worst hit on the bourse, losing 6.48 per cent and 5.17 per cent, 
respectively.<br>
<br>
Research Highlights<br>
UFC’s performance in FY07 has been commendable with loan book growth of 67 per 
cent YoY and net income growth of 87 per cent YoY (excluding extraordinary 
items) to RO2.73 million despite cutthroat competition in the leasing sector. 
UFC has demonstrated its ability to grow its loan book on the back of good 
funding network and credit quality. UFC is the market leader in the non-banking 
finance companies (NBFC) market in terms of loan book size and network reach. 
(Market share is calculated based on adjusted gross loans for unearned interest 
income). <br>
<br>
Currently, UFC and Al Omaniya (AOFS) are the only large players in the NBFC 
space, with approximately 50 per cent of the total leasing receivables in the 
sector. Albeit any imminent change in leasing sector dynamics, expected after 
the merger of National Finance and Muscat Finance, UFC is expected to hold its 
market position. The merger is expected to create a very close competitor for 
UFC, with almost similar receivable portfolio and branch network reach. The 
merged entity is also expected to enjoy better funding. Despite all these 
hassles, we are conservatively optimistic about UFC’s performance going forward, 
considering its track record of out-beating competition, leveraging its network 
reach and new products, which should help sustain growth and market share. 
Moreover, Oman’s growing economy is all set to provide good opportunities to 
this market lender.<br>
<br>
UFC’s significant receivables addition during Q4 FY07 and given the falling 
interest rate scenario, the company seems to be in a strong position to 
capitalise on the market trend. The LIBOR rate has almost decreased by 250 basis 
points (bps) from January 2007 to March 2008. From December 2007 till March 
2008, LIBOR rates have fallen by 200 bps. This suggests that the company should 
be able to refinance its debts at a lower cost given its maturity profile (see 
chart below) while charging higher interest rate on the existing loans.</font><p align="justify" style="margin: 3px 10px">
