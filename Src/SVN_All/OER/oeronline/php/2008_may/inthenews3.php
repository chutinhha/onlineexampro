<p align="justify" style="margin-left: 10px; margin-right: 10px">
<font face="Verdana" size="1">
<b>The new ADSL service complements the Sultanate’s efforts to disseminate a 
digital culture all over the country, and allows individuals, businesses, 
commercial and educational institutions to select packages that suit their 
requirements.</b><br>
<br>
<b>
<img border="0" src="in_news3.jpg" align="right" hspace="4" vspace="4" width="250" height="208"></b>Oman Telecommunications Company (Omantel) recently unveiled a suite of broadband 
Internet packages, offering the Gulf region’s fastest service for home and 
business users, up to a maximum speed of 8 Mpbs and 16 Mpbs respectively. In 
all, 13 ADSL packages, targeted at all segments of society, including special 
offers for schools and charities, were introduced. Said Dr Mohammed bin Ali al 
Wohaibi, Chief Executive Officer of Omantel, “This is the first time Internet 
users in the Sultanate are being offered such high speed ADSL services at 
equally competitive rates. The new services place Omantel in the lead position 
among Internet service providers (ISP) in the Gulf region.”<br>
<br>
The existing ADSL service for home users, costing RO12 a month, which runs at a 
speed of 384 Kpbs will be upgraded to 512 Kpbs, without any additional charges, 
while the other four packages have speeds ranging from 1 Mpbs, 2 Mpbs, 4 Mpbs 
and 8 Mpbs, priced at RO19, RO29, RO39 and RO99 respectively a month. The 
services have various free add-ons, including free traffic ranging from 2 Gb to 
unlimited traffic, anti-virus/spam and free e-mail facility ranging from 1 to 7 
accounts. “Omantel is launching a very advanced level of Internet service, which 
can offer up to 24 Mpbs speed. The roll-out of the new service has cost RO10 
million in investment,” Dr Al Wohaibi said.<br>
<br>
The nation-wide broad band network expansion will provide 180,000 ADSL lines, he 
added. He called the investment in ADSL services a wise decision as it would 
provide huge capacity as well as superb speed. The French telecom company Orange 
served as consultant for the new ADSL service roll-out and they are fully 
involved in its implementation, Dr Al Wohaibi pointed out. “Omantel is expecting 
a significant growth in ADSL subscription, projecting 75,000 new connections 
this year compared to the existing 29,000 subscribers,” he said. “We are 
encouraging dial-up customers to upgrade their connections to ADSL services,” he 
said.<br>
<br>
Currently, there are 53,000 dial-up customers and 24,000 pre-paid Internet 
customers in the Sultanate, he added. Omantel has put in place a next generation 
ADSL network using IP-based technologies for voice, video and data transmission. 
Wireless Internet services are ideal for personal use for people on the move 
whereas ADSL facilitates heavy use at home and offices. Omantel is considering 
introducing Wi-Max for wireless Internet services and discussions are under way 
with the Telecommunications Regulatory Authority in this regard, Dr Al Wohaibi 
said.<br>
The new ADSL service complements the Sultanate’s efforts to disseminate digital 
culture all over the country, he added. The new service, said Dr Al Wohaibi will 
enable individuals, businesses, commercial and educational institutions to 
select packages that suit their requirements. Value-addition services for the 
different ADSL packages include free emails, free anti-virus and spam security 
solutions and free downloads or in some cases with a nominal fee.</font></p>
