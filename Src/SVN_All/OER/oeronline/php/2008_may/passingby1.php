<b><font face="Verdana" size="4">Making it to the top<br>
</font></b><font face="Verdana" size="1"><br>
<img border="0" src="passing1.jpg" align="left" hspace="8" vspace="6">The UK is the largest investor in Oman. But there is much more that can be 
done, feels David Lewis, the Lord Mayor of the City of London, who was in town 
last month to promote business between the UK and Oman. 
<b> <br>
</b><br>
The Lord Mayor spent two days in Oman as part of his 11-day tour of the four GCC 
countries –Kuwait, Bahrain and Saudi Arabia besides the Sultanate, accompanied 
by a strong business delegation. The delegates included 16 key stakeholders from 
Arab-British Chamber of Commerce, HSBC, National Bank of Kuwait, 
PricewaterhouseCoopers’ UK T&amp;I, Strutt &amp; Parker, Futures and Options 
Association, Currencies Direct, Jasper Capital, Trowers &amp; Hamlins, North Asset 
Management, Standard Chartered Bank, JP Morgan, William Knight &amp; Associates, 
MerchantBridge, Citigroup, ETQ Initiative, etc.<br>
<br>
On the response from Omani government and corporate community, he said, “We met 
various ministers and business leaders. The reaction has been very positive. We 
are trying to explore the areas in which we can increase our investment here and 
Omani investment in Britain. We are interested in sectors, including 
infrastructure development, which is expected to attract investments of US$40 
billion or more in Oman over the next few years. We are also interested in 
further expansion of the capital market in Oman as we have strengths in that 
area. We are also keen to focus on education, training and qualification. With 
60 per cent of the population under the age of 21 in Oman, we see a great scope 
for our educational institutions here.”<br>
<br>
<b>Opportunities in GCC</b><br>
Talking about the investment from GCC to UK, Lewis said, “There is a great deal 
of investment being made in the property in London. Quite a lot of it has come 
from the Gulf region.” After 9/11, the investment from the Arab world in western 
markets, such as the US and Europe, has undergone a paradigm shift. According to 
the Lord Mayor: “9/11 has changed the world. Interestingly, if we look at the 
London market, we have improved our position vis-à-vis other financial centres 
over the last few years. The credit goes to our people. We offer free trade, 
competitive environment and multicultural society. I’m not one for criticizing 
New York. Yes, independent reports suggest that London is slightly ahead of New 
York. But actually what is really important is that New York and London are 
ahead of everyone else because we are interdependent. It is an age of 
globalisation, there is a huge presence of Americans in London. We need them and 
they need us. We welcome investment from overseas, specially sovereign wealth 
funds in the Gulf. ”<br>
<br>
On the issue of delay in the European Union and GCC FTA, the Lord Mayor said, 
“It is very unfortunate that the agreement has got bogged down. It is 
comparatively easy (for AGCC) to sign bilateral FTAs with the US but the same is 
not the case with the EU, which has many countries as its members. We are hoping 
that the agreement will be signed in December”. The Lord Mayor serves a 
ceremonial and social, rather than any political function and primarily promotes 
London’s business interests abroad. The Lord Mayor serves a one-year term, 
during which time he travels extensively around the world to promote the Square 
Mile and Britain’s financial community. As the head of the City of London, the 
Lord Mayor presides over its governing bodies, the Court of Aldermen and the 
Court of Common Council. He is also Chief Magistrate of the City of London and 
Admiral of the Port of London.<br>
&nbsp;<br>
<b>The early days</b><br>
From a humble background, David Lewis has risen to this coveted position after 
spending almost four decades in a law firm. On his early days, he stated in an 
interview, “My father went to Malaya as a teacher, was locked up in a Japanese 
POW camp. I went to the Army School in Singapore, which was part of Malaya in 
those days… I don’t think I learnt much. It was a huge shock when my parents 
retired early to the UK and I was sent, at eight, as a boarder to the Dragon 
School in Oxford. I was behind the others, and in my first term, was bottom of 
the school in every single subject. Gradually, with much help and prodding, I 
moved up the class and began to enjoy Latin, Greek and history. Amazingly, I was 
appointed head boy in my last term.” Talking about his entry in the law 
industry, he said, “In my days, law was a subject in which it was easier to get 
into Oxford. I knew nothing about law but I remember the first paper I studied 
for was criminal law and we started with murder. I thought, ‘This is 
interesting!’ ”<br>
<br>
<b>– Ramesh Kumar and Akshay Bhatnagar</b><br>
