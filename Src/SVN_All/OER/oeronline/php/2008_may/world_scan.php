<font size="2" color="#CC3300" face="Verdana"><b>India’s main index suffers 
biggest quarterly decline since 1992</b></font><font face="Verdana" size="1"><br>
<img border="0" src="world_scan2.jpg" align="left" hspace="6" vspace="2" width="150" height="162">Indian 
shares fell 4.44 per cent to end the March quarter down 22.9 per cent, their 
biggest quarterly fall since the June 1992 quarter, as surging inflation and 
signs of an economic slowdown scared off investors.<br>
<br>
However, despite the sharp fall in the March quarter the index ended the fiscal 
year up 19.7 per cent due to a strong rally in 2007. Financial stocks were hit 
hard, hurt by persistent concerns of a global credit crunch and data for the 
month showed a rise in local inflation to a 14-month high of 6.7 per cent in 
mid-March. Top private lender ICICI Bank fell 7.8 per cent to Rs770.10, taking 
its losses for the quarter to 37.5 per cent and contributing most to the fall in 
the main index. <br>
<br>
“Inflation is the biggest problem right now, and with the economy slowing down 
there will be a pressure on margins of corporates that will lead to an earnings 
downgrades,” said Nikunj Doshi, investment manager at Envision Capital.<br>
