<font size="2" color="#CC3300" face="Verdana"><b>UBS reveals another $19 billion 
subprime loss</b></font><font face="Verdana" size="1"><img border="0" src="world_scan1.jpg" align="right" hspace="6" vspace="2" width="150" height="154"><br>
Swiss Bank UBS became the world’s worst-hit bank in the US mortgage crisis, when 
it recently revealed a second round of subprime-related losses amounting to $19 
billion. The write-down plunges UBS into a net loss of 12 billion Swiss francs 
(7.6 billion euros) for the first quarter of this year and comes after the bank 
announced an $18.4 billion write-down in 2007.<br>
<br>
The overall write-downs of UBS so far totals $37.4 billion. UBS said that it was 
cutting exposure to risky positions and its stock contained $31 billion in 
subprime mortgage related positions.<br>
<br>
The bank’s problems continued in the first quarter with pre-tax losses of about 
18 billion Swiss francs. <br>
