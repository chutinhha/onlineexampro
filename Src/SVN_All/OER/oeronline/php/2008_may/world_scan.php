programmes, according to a report by The Arab Petroleum Investments Corporation 
(Apicorp). Though the report forecasts lucrative gains for the region owing to 
high oil prices, it also warns of a likely downturn in the region’s energy 
investments if global growth slows. The expansion in the emerging markets and 
the growth momentum in China and India would not be sufficient to offset slower 
growth in the US and other key OECD countries, it said. “As a result growth is 
expected to decelerate from 4.9 per cent in 2007 to 4.1 per cent in 2008,” the 
report said. However, the extent to which the Arab region would continue to be 
sheltered against the deteriorating external environment remained uncertain, it 
said.<br>
