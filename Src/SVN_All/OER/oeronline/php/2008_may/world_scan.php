<font size="2" color="#CC3300" face="Verdana"><b>US top banks shunning Iran </b>
</font><font face="Verdana" size="1"><br>
The world’s top banks have largely stopped dealing with Iran in response to US 
and UN sanctions, according to a senior US Treasury official. The move reflects 
a universal stand by industrialised countries on ways to combat financing for 
weapons proliferation, according to Stuart Levey, the Treasury’s undersecretary 
for terrorism and financial intelligence.<br>
<br>
The Treasury has blacklisted several Iranian state banks, including its largest, 
Bank Melli, on grounds of them supporting Tehran’s alleged nuclear development 
programmes. <br>
The US sanctions against Iranian banks prohibit Americans from doing business 
with them and seek to freeze any assets they may have under US jurisdiction.<br>
