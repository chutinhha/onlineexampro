<font face="Verdana" size="1"><br>
</font><b><font face="Verdana" size="4">Why companies need to implement 
corporate governance</font></b><font face="Verdana" size="1"><br>
<br>
<b>
<img border="0" src="in_news2.jpg" align="right" hspace="4" vspace="4" width="300" height="182"></b>As companies world over face external and internal challenges that may adversely 
affect their competitiveness, it is important to implement sound corporate 
governance laws. This formed the basis for the recently concluded corporate 
governance conference conducted by the Capital Market Authority of Oman. HE 
Sayyid Abdullah Bin Hamed Al Busaidy, President of the State Audit Institution, 
who presided over the event, emphasized the role of sound practices to “enhance 
the success and continuation of companies in the light of challenges associated 
with globalization, information revolution, economic transparency, regional 
blocs and international competition”.<br>
<br>
The conference drew participation from a number of local and international firms 
among them World Bank, Oman LNG, Dun &amp; Bradstreet and Trowers &amp; Hamlins. The 
speakers discussed the importance of corporate governance in a free market 
driven economy and corporate governance trends that have witnessed remarkable 
changes and developments. <br>
<br>
“Corporate governance is a necessity for the development of financial markets in 
line with the legislations and regulations of international financial markets 
that abide by the principles of fairness, efficiency, disclosure and 
transparency, and serves national interests. It enhances the confidence of 
investors and market participants as well as the confidence of financing 
institutions,” said Al Busaidy. <br>
<br>
The seminar demonstrated how corporate governance aims at improving the 
performance of companies and increasing their efficiency to maximise profit and 
the long-term value of shareholders. In Oman, the concept of corporate 
governance has been remarkably developed since the setting up of the CMA. 
Following a CMA conference on corporate governance held in 2002, a comprehensive 
code of corporate governance was issued in 2002 and the commencement of partial 
implementation followed, which gave companies ample opportunity to implement and 
adapt the provisions of the code until full fledged implementation was made 
possible in January 2004.<br>
<br>
Recently, last year, the CMA also established a department for corporate 
governance and early this year a committee of corporate governance was formed 
comprising members from public and private sectors to activate and establish 
rules of corporate governance, disseminate and consolidate corporate governance 
and upgrade the present code, said Al Busaidy. In addition, the CMA is in the 
process of preparing a guide for non listed companies at the capital market 
especially of the government companies. The conference included three working 
sessions that highlighted the importance of governance, the government and its 
role in developing better governance, the responsibilities of the board of 
directors and role of a panel discussion on the future of governance. The 
conference was targeted at chairpersons, directors and executive managements of 
joint stock companies, closed joint stock companies, government and private 
companies. </font></p>
