                    Forecast for Oman<br>
                    </b>According to the WTTC report, travel and tourism is 
                    expected to generate RO792.9 million (US$ 2,062.1 Million) 
                    of economic activity (total Demand) in 2002, growing 
                    (nominal terms) to RO1,616.4 million (US$4,203.8 million) by 
                    2012. The demand is expected to grow by 6.3 per cent p.a., 
                    in real terms between 2002 and 2012. The industry is 
                    expected to contribute 1.3 per cent to the GDP in 2002, 
                    (RO94.5 million or USS$245.8 million), rising in nominal 
                    terms to RO151.5 million or US$394.1 million (1.3 per cent 
                    of total) by 2012. <br>
                    <br>
                    The industry's economic contribution (per cent of total) 
                    should rise from 6.7 per cent (RO490.2 million or US$1,274.9 
                    million) to 7.1 per cent (RO825.1 million or US$2,145.9 
                    million) in the same period. In 2002, the travel and tourism 
                    economic employment is estimated at 44,173 jobs or 7.3 per 
                    cent of total employment, which is one in every 13.7 jobs. 
                    By 2012, this should total 55,219 jobs, 7.7 per cent of 
                    total employment or one in every 13 jobs. <br>
                    <br>
                    The 10,910 industry jobs account for 1.8 per cent of total 
                    employment in 2002 and are forecast at 12,991 jobs or 1.8 
                    per cent of the total by 2012. Travel and tourism is 
                    expected to generate 11.7 per cent of total exports (RO398.2 
                    million or US$1,035.7 million) in 2002, growing (nominal 
                    terms) to RO905.7 million or US$2,355.6 million (11.9 per 
                    cent of total) in 2012. Capital investment is estimated at 
                    RO133.5 million, US$ 347.1 million or 6.7 per cent of total 
                    investment in year 2002. By 2012, this should reach RO216 
                    million, US$561.7 million or 6.8 per cent of the total.<br>
