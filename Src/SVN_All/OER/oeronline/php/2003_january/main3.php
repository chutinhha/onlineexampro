                  Bright prospects, but...</font><p><b><font face="Verdana" size="1">
                  Not very long ago, Gulf countries that seek diversification 
                  from oil and aspire to integrate with the world economy, have 
                  developed policies that could strengthen their tourism 
                  product. A recently released world tourism report states that 
                  these countries do stand to benefit from a recovering global 
