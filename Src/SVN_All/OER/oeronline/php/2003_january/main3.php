                    <p><font face="Verdana" size="2">Anybody wishing to 
                    experience the true essence of Arabia might like to know 
                    that - it is just not Dubai anymore, others are opening up 
                    fast. Oman, with all its natural assets, has already set 
                    itself on the high growth path; Bahrain is planning comeback 
                    strategies; and closed Saudi Arabia is getting liberal 
                    towards tourists. All this, clubbed with the GCC union, 
                    means that the member countries are gearing up to carve 
                    their own niche in the market. Almost all of them are 
                    projecting growth in the coming years and have aligned their 
                    strategies in such a way that regional tourism gets as much 
                    leverage as would purely inbound tourism.<br>
                    <br>
                    <b>New trends<br>
                    </b>A detailed research report produced by the World Travel 
                    and Tourism Council (WTTC) states that the travel and 
                    tourism industry in the Middle East is expected to 
                    contribute 3.6 per cent (around US$30.3 billion) to its 
                    gross domestic product (GDP) in 2002, and shall increase to 
                    4.2 per cent (US$50 billion) by 2012. Clearly, the report 
                    indicates more than 50 per cent growth for the Middle East 
                    region, and reveals that the demand for travel shall grow by 
                    4.4 per cent p.a. in real terms between 2002-2012. Also, 
                    employment within the tourism industry in the Middle East 
                    shall jump from 2.99 million in 2002 to 4.13 million in 
                    2012, an 8.4 per cent increase. <br>
                    <br>
                    The WTTC&quot;s report, developed on a concept called Tourism 
                    Satellite Account (TSA), is a comprehensive simulation of 
                    the new standards adopted by the United Nations. 
                    Essentially, TSA is based on a demand-side concept of 
                    economic activity, because the industry does not produce or 
                    supply a homogenous product or any service like traditional 
                    industries. Instead, travel and tourism is an industrial 
                    activity defined by the diverse collection of products and 
                    services that are delivered to visitors.<br>
                    <br>
                    The report, in its essence, considers the post 9/11 period 
                    as a defining moment in the history of world tourism and 
                    states that the travel and tourism industry would require a 
                    longer time to recover than expected. This is because, for 
                    most of the last year the demand for tourism was lower and 
                    spending cutbacks were associated with the global economic 
                    downturn. <br>
                    <br>
                    'However, Q4 output has been well above, therefore the 2002 
                    end does not look bleak. In fact, the relative strength of 
                    consumer spending in many countries, with expectations 
                    earlier in the year has helped maintain personal travel and 
                    tourism spending. We now expect the industry output flat in 
                    2002 rather than the declining levels. With output flat, the 
                    industry jobs have fallen this year by an estimated 1.7 per 
                    cent, leaving three million fewer jobs in 2002 than in 
                    2000,Ó says WTTC. <br>
                    <br>
                    <b>The past<br>
                    </b>In another report, the Madrid-based, World Tourism 
                    Organisation (WTO), had earlier in 2002 stated, 'Tourism 
                    recovery is underway. Fear is progressively fading away and 
                    business is gradually returning to normal although some 
                    destinations might be negatively influenced by the fact that 
                    operators have reduced capacity. Recovery is most visible in 
                    intraregional travel, and in the regions of the Pacific, 
                    Europe and Africa.'<br>
                    <br>
                    WTO had also said that US$463 billion international tourism 
                    receipts were recorded worldwide in 2001 (down by 2.6 per 
                    cent over 2000). Tourists shifted their travel habits during 
                    this period of time, they chose closer and less expensive 
                    destinations. Half of the receipts were collected by Europe, 
                    and almost a third by the Americas (17.7 per cent), followed 
                    by East Asia and the Pacific (17.7 per cent), Africa (2.5 
                    per cent), Middle East (2.49 per cent) and the last in the 
                    list, South Asia taking one per cent.<br>
                    <br>
                    The aforesaid figures indicate how much work still needs to 
                    be done by the Middle East countries in order to clinch a 
                    big pie.<br>
