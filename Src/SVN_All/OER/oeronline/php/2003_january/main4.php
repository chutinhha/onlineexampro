                  Rhythm divine...</font><p><b><font face="Verdana" size="1">
                  Mercedes-Benz has a reputation for delivering cars that offer 
                  tantalising luxury. OER takes the new S-Class for a spin and 
                  reveals why this image has been enhanced</font></b></p>
                    <p><font face="Verdana" size="2">In Oman, the S-Class from 
                    Mercedes-Benz makes a welcome reappearance. We took the 
                    S350L for a test-drive, the long wheel-base model which is a 
                    successor to the S320L. The modifications certainly give the 
                    car more appeal but car buyers may regard the 2003 model as 
                    a moderate facelift, compared to rival luxury brands that 
                    are also packed with new features and gadgetry. Despite 
                    this, Mercedes-Benz has proven once again that it is 
                    possible to make a great car feel nicer if not 
                    easier-to-drive. <br>
                    <br>
                    <b>Exterior</b><br>
                    One of the most noticeable design changes to its sleek 
                    appearance is that it has developed more of a sloping front 
                    fascia with a radiator grille that has moved below the 
                    bonnet line. Another difference is the use of bi-xenon 
                    headlamps which far outshine the car's former front lights. 
                    A dirt-free boot lid handle is another welcome addition 
                    designed to ensure that you can open the boot with minimum 
                    hassle. Viewed from the side, the car's new 16-inch, 
                    seven-hole, light alloy wheels are another asset.<br>
                    <br>
                    <b>Interior</b><br>
                    The S350L has the keyless-go-drive authorisation system that 
                    opens the door by touching its handle. Starting the engine 
                    is just as simple by first applying the brake and pressing a 
                    button on the gear lever.<br>
                    Inside the S350L, a large number of features have been 
                    redesigned to provide more comfortable and luxurious 
                    surroundings. The discreet chrome strips that adorn the chic 
                    wood trim inside the cabin, further improve seat upholstery 
                    and stylish control switches are examples enough of the 
                    meticulous attention that has gone into the model refinement 
                    package. Merc's customers can opt for an exclusive interior 
                    appointments package called designo couture to deck out the 
                    cabin with even more sumptuous furnishings.<br>
                    The company has totally modified the S-Class seats - 
                    introducing a softer design and new upholstery technology. 
                    All seats have a feature that provides inflation of air to 
                    bolster lower back support while the front seats deliver 
                    airflow through tiny chambers to control your seat climate. 
                    An optional extra brings 'multicontour' seats to driver and 
                    front passengers that gently pulsate to provide a massage to 
                    the shoulders and lower back. If you pay more, you can enjoy 
                    'dynamic multicontour' seats for superlative lateral support 
                    when cornering. When the vehicle enters a left-hand bend, 
                    for instance, the system pumps more air into the chambers on 
                    the right-hand side of the backrest to restore balance in 
                    your seating position. The automatic climate control system 
                    throughout the car is excellent. <br>
                    The centre console fitted snugly between the front seats 
                    offers sophistication to match the dash board and groups 
                    together the audio and television system, climate control, 
                    navigation system, cell phone controls, hazard warning 
                    lamps, ESP activation button, and the parking-assist system 
                    called Parktronic.<br>
                    The tilt/telescope steering wheel is also a computerised 
                    marvel with buttons to control more than 50 functions. Other 
                    goodies include a sliding sunroof, automatic side mirrors, 
                    and automatic windows. Those still checking for changes from 
                    before will notice a new cup holder below the gear lever and 
                    the cell phone holder conveniently flips to one side thereby 
                    offering enough space for you to use the armrest while using 
                    the phone in its holder.<br>
                    <br>
                    <b>Navigation</b><br>
                    The S350L we tested was equipped with Merc's latest GPS 
                    navigation system that included a digital map of Muscat, the 
                    UAE and other GCC countries. This is a standard feature on 
                    all S-Class long wheel-base 2003 models.<br>
                    The COMAND system can be either used practically or just for 
                    show. We decided to put it to test asking it to navigate the 
                    car to a prominent petrol station at Sarooj from a hotel 
                    based in Shatti Al Qurum. Although it was a little 
                    befuddling at first, its usefulness became clear, 
                    particularly when the system's directions were actually 
                    obeyed by the driver. The screen is large, clear and 
                    colourful and apparently presents crisper visuals than its 
                    predecessor. A useful unassuming arrow pops up close to the 
                    speedometer to indicate how far you are away from your next 
                    turning while you are given directions in a polite, 
                    pre-recorded fashion. <br>
                    Easy-to-reach buttons for switching on the navigation system 
                    while driving are at your fingertips on the steering wheel 
                    as well as a lever that activates voice control to help you 
                    switch from it to the CD, radio, television or mobile phone.
                    <br>
                    <br>
                    <b>Safety</b><br>
                    Safety issues are again well addressed by Mercedes-Benz. An 
                    anti-lock braking system (ABS), Acceleration Skid Control, 
                    and Brake Assist come as standard equipment for the S-Class. 
                    This ESP (Electronic Stability Program) system helps the 
                    driver maintain directional stability both while driving 
                    straight and turning corners. This, too, can over-ride your 
                    decision-making by sensing under-steer or over-steer and 
                    then increasing the necessary brake pressure to put you back 
                    where you belong. Hey presto, the S350L comfortably 
                    negotiates sharp corners, even on steep climbs, and deflects 
                    less during critical stops. <br>
                    An extra option is Distronic, a system that tries to keep 
                    you from tailgating, taking charge of the gas pedal or brake 
                    when necessary. To ensure the driver pays attention, a 
                    pictogram shows the proximity of the car in front.<br>
                    Pride of place among the leading edge, state-of-the-art 
                    innovations of the S350L goes to Pre-Safe. It is a 
                    preventive occupant protection system that heralds a new era 
                    in automotive safety, according to Mercedes-Benz. Pre-Safe 
                    can detect the threat of a collision in advance and initiate 
                    preventive measures to prepare the occupants and vehicle for 
                    impact. It is a response to the company's observation that 
                    in many vehicular accidents, there is a window of 
                    opportunity between the driver's recognition of the 
                    impending accident and the actual accident itself.<br>
                    Pre-Safe warns against dangerous situations by using sensors 
                    belonging to the ESP and brake assist features. Should an 
                    accident seem imminent, the system applies split-second 
                    tensioning of the seatbelts, moves the front passenger seat 
                    and power-adjustable individual seats in the rear to the 
                    most favourable position and closes the sunroof (if the 
                    vehicle is skidding). If the accident is averted at the last 
                    moment, the seatbelt tensioning is cancelled while the seats 
                    and sunroof reset to their original positions. We didn't 
                    give Pre-Safe a trial. Knowing that it was there brought 
                    substantial peace of mind nonetheless.<br>
                    Otherwise, safety is supplied by eight airbags: one on the 
                    steering wheel, one adjacent to the glove compartment, one 
                    on each door panel, and one on each side. A fire 
                    extinguisher, warning indicators for brake pad wear, 
                    hands-free cell phone speakers and mic through a 
                    voice-activated system called Linguatronic, childproof door 
                    locking system, and 3-point seatbelts all contribute to 
                    lowering accident and injury risk too.<br>
                    <br>
                    <b>Performance</b><br>
                    The S350L drives as well as it looks. Increasing the 
                    displacement of the V6 petrol engine to 3.7 litres enables 
                    output to be boosted by 12.5 per cent to 245 horsepower at 
                    5,700 rpm. Acceleration is breathtaking in its silky rush to 
                    devour the road. Shifts are just about undetectable. 
                    Mercedes claim the S350 has the capability to accelerate 
                    from zero to 100 km/h in 7.3 seconds. It doesn't guzzle fuel 
                    as first you might imagine. Fuel consumption in the new 
                    S350L - 11.1 litres per 100 km - remains at an exemplary 
                    levels for this displacement class making it one of today's 
                    most efficiently potent engines. <br>
