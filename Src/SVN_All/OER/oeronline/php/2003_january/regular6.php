                  were working in the high end of the market; when another high 
                  quality jeweller made his entry into Oman. This compelled us 
                  to shift our focus towards the fashion segment of the 
                  jewellery market. Now we are working towards the corporate 
                  market, for which we've just bought new machinery. At Jawahir 
                  Oman we can be very flexible, since we manufacture most of our 
                  jewellery inhouse, and unsold stock can always be melted down 
