                  Is OPEC losing touch?<br>
                  </font><b><font face="Verdana" size="1"><br>
                  Crisis in Venezuela, a stagnant world economy, increasing oil 
                  output from non-OPEC rivals, the threat of war in Iraq - the 
                  list is near endless. For the time being, politics seems to be 
                  dominating economics in the oil price equation</font></b><br>
                  <font face="Verdana" size="2"><b><br>
                  </b>In
                  late December 2002, oil cartel OPEC announced that Qatari oil 
                  minister HE Abdullah Al Attiyah would be its new President. 
                  Attiyah takes the reigns on January 1, 2003 and while the job 
                  represents a major personal achievement, it also represents a 
                  big challenge. These are troubled times for the organisation, 
                  with a host of forces outside its control pushing and pulling 
                  oil prices in opposite directions.<br>
                  <br>
                  Take Venezuela, OPEC's third largest producer after Saudi 
                  Arabia and Qatar. In mid-December the country was in the grip 
                  of political and economic turmoil, as a two-week general 
                  strike brought the oil industry to its knees. Workers who went 
                  on strike succeeded in preventing meaningful oil exports 
                  leaving the country - during the second week in December, two 
                  tankers left Venezuela, compared with the normal traffic of 
                  close to 100 tankers. <br>
                  <br>
                  The crisis sent the price of the OPEC crude oil basket to 
                  US$28.61 per barrel on December 16, 2002. That's beyond the 
                  cartel's target band of US$22 to US$28 per barrel, a move that 
                  sends alarm bells ringing not just within the industry, but 
                  throughout the global economy, which fears that high oil 
                  prices may stifle the already sluggish recovery. 'The high 
                  price that you see is a political price,' said Attiyah, 
                  arguing that it was therefore outside his control.<br>
                  <br>
                  <b>Disturbed atmosphere</b><br>
                  The feeling that OPEC is losing its grip on the market was 
                  underlined by the cartel's secretary general, who admitted 
                  that it was too weak to resist such powerful political forces. 
                  'OPEC cannot single-handedly maintain market equilibrium,' 
                  said Alvaro Silva Calderon, secretary general. 'When we look 
                  at current global oil demand, we see only a slight 
                  improvement, yet prices remain buoyant. This shows us that 
                  factors other than those associated with the fundamentals of 
                  the market are influencing prices,' he explained.<br>
                  <br>
                  The cartel's only weapon is oil supply, and this leaves 
                  members in a quandary. On one hand, the Venezuela situation 
                  means that the market has been crying out for more oil from 
                  the likes of Saudi Arabia, the UAE and Kuwait, all of which 
                  have significant excess capacity. On the other hand, OPEC is 
                  wary of flooding the market in response to a short-term 
                  incident, for fear of sending prices nose-diving, as has 
                  happened in the past (members still remember the dark days of 
                  US$10 per barrel in 1998 and early 1999).<br>
                  <br>
                  'If we enter this low-demand period with excess supplies in 
                  the market, prices will surely come under pressure,' warned 
                  Calderon. With Russian oil production forecast to increase by 
                  10 per cent next year, this is a major headache for OPEC. 
                  Incoming President Attiyah echoed those sentiments. 'The high 
                  oil price is not related to a shortage of supply in the 
                  market. There's enough oil in the market. We should not build 
                  our decision on political problems.' On December 19, 
                  Venezuela's fellow OPEC members were still refusing to pump 
                  more crude to make up for the South American shortfall.<br>
                  <br>
                  Almost as important for the cartel is the fact that pumping 
                  more barrels would fly directly in the face of its new quotas, 
                  agreed in early December. Members met in Austria on December 
                  10, when they agreed to raise their output quotas for the 
                  first time since January 1, 2002. The move was much needed - 
                  at the time; OPEC 10 members (excluding Iraq) were producing 
                  some three million barrels per day (bpd) above their 
                  self-imposed collective quota of 21.7 million bpd, making a 
                  mockery of the cartel's quota system.<br>
                  <br>
                  Crucially, though, the increase in official quotas to 23 
                  million bpd was merely an increase on paper. In reality, 
                  members pledged to cut actual production by 1.7 million bpd, 
                  in a bid to shore up prices in the face of an increasingly 
                  bleak global economic outlook. If OPEC increases supply now, 
                  hopes of rebuilding its credibility will take a major blow.<br>
                  <br>
                  Michael Fitzpatrick, a broker at Fimat USA Inc. in New York, 
                  believes that OPEC's most likely response is to do nothing - 
                  neither raise production to compensate for Venezuela, nor cut 
                  output in line with the new quotas. 'OPEC has always been 
                  reluctant to cut back on production when prices are this 
                  high,' he said. 'Venezuela news continues to dominate the oil 
                  price picture. It's unlikely that OPEC will make much of an 
                  effort to fulfil its recent agreement (to cut production) 
                  because of the strike.'<br>
                  Looking further into 2003, the London-based Centre for Global 
                  Energy Studies (CGES), an influential think-tank, argues that 
                  OPEC needs to cut output by just under one million bpd from 
                  recent levels to keep oil prices close to its US$25 per barrel 
                  price target. However, given the uncertain nature of both 
                  Iraqi and Venezuelan oil supplies, this target may become 
                  obsolete.<br>
                  'The loss of Venezuelan oil already exceeds the reduction 
                  needed from OPEC,' said the CGES in its monthly oil market 
                  report, published in mid-December. Coupled with the threat of 
                  war in Iraq, CGES analysts warned that there is 'considerable 
                  upside potential for prices in the immediate future.' <br>
                  Indeed, CGES argues that OPEC is right to take a cautious 
                  approach to meeting the Venezuelan shortfall, as short-term 
                  acts can have medium-term consequences. If producers open the 
                  taps too far, keeping OPEC's total crude output at around its 
                  November level of 26 million bpd (including Iraq, which 
                  produces around 2.4 million bpd), crude oil inventories will 
                  begin to build. And a modest 0.6 million bpd stock build in 
                  the Q1 of 2003 would undermine prices for the rest of the 
                  year, warns the CGES. That would require further output cuts 
                  from OPEC to prevent prices from dropping below US$22 per 
                  barrel when demand eases in the warm Western summer. <br>
                  For all these reasons, Abdullah Al Attiyah's introduction to 
                  life as OPEC President promises to be stormy one.<br>
