            <font face="Verdana" size="1"><b>This article is the second in a 
            2-part series exploring the potential of developing an Oman brand, 
            and how such a brand could support economic development in Oman. By 
            Tom Farole<br>
            </b>
            <br>
            Ask Omani businessmen whether they would source their telecom 
            supplies from within Oman or from Germany/Sweden. <br>
            Nationalism aside, chances are that a majority of them would choose 
            the latter options. But the fact remains that an Omani company has 
            supplied optical fibre to megacorps such as Siemens and Ericsson. A 
            look into the shopping baskets of Omani consumers at the 
            hypermarkets would reveal packages with international brand names. 
            But the fact remains that these international food companies source 
            some of their most critical ingredients from Omani manufacturers.
            <br>
            In a rapidly modernising country like Oman, there is often a 
            tendency to assume that what comes from abroad is generally superior 
            to what is produced domestically. Often, this assumption is highly 
            exaggerated. When faced with the marketing power of international 
            brands, consumers, business buyers and even government are sometimes 
            unaware of what is produced locally. <br>
            <br>
            <b>Why &quot;Omani-Made&quot; matters </b><br>
            As part of the process of Oman joining the WTO in 2000, barriers to 
            imports dropped significantly. This has been further reinforced by 
            the implementation of the GCC Customs Union at the beginning of this 
            year. The effect of these initiatives has been to open up the Omani 
            market to an ever-wider range of international products and 
            services. <br>
            This is by no means a bad thing. Studies show that the long-run 
            benefits of import openness more than offset the short-term problems 
            of job loss and industrial transition. But many domestic industries 
            in the early stage of development, particularly businesses in the 
            SME (small and medium enterprises) sector, will struggle to compete 
            with international competitors due to their limited capacity to 
            build and promote a brand. In many cases, these domestic producers 
            may be delivering international levels of quality at highly 
            competitive prices, but have neither the financial resources nor the 
            capabilities to communicate this to local and international markets.
            <br>
            Promoting the development of domestic industry through a national 
            branding initiative can play a significant role in the economic 
            development of Oman. First and foremost, increasing the domestic 
            demand for Omani goods and services will lead directly to job 
            creation and will strengthen the establishment of nascent industry 
            sectors. Aside from this, developing a brand to promote Omani-made 
            products can also provide a platform to meet other key objectives of 
            Oman's development strategy, including Omanisation, environmental 
            protection and quality/innovation. <br>
            <br>
            <b>The Omani branding initiative </b><br>
            Government and industry in Oman have long recognised the importance 
            of branding and the value of promoting domestic products. Back in 
            1998, a national branding initiative was launched by the Ministry of 
            Commerce and Industry and the Oman Chamber of Commerce and Industry. 
            The programme, dubbed &quot;The Omani Product is our First Choice&quot; 
            included the development of a logo and the launch of a number of 
            promotional initiatives to support Omani-made products. These 
            included education and communication, travelling, Omani product 
            exhibits and rebates on advertising expenditure for companies 
            involved in the programme. <br>
            This initial programme has been successful on a small scale. 
            However, in comparison to similar programmes launched in places like 
            South Africa (see case study on the following page) and Australia, 
            it hasn't yet had a dramatic impact on awareness and purchasing 
            patters of consumer and business buyers. With Oman's continued 
            development and openness to world trade, now may be the time to 
            re-launch the national brand and reinvigorate the promotion of 
            Oman-made products both to domestic and international buyers. The 
            potential is significant Ð achieving it is just a matter of careful 
            planning and execution. <br>
            <br>
            <b>Keys to success </b><br>
            As countries like South Africa realised, developing an effective 
            national branding programme requires a lot more than just a stylish 
            logo and a few advertisements on CNN. National branding programmes 
            must be developed holistically, with clear objectives, a strong 
            organisational structure and wide-ranging set of initiatives. They 
            must also be sustainable to be effective. There are seven key issues 
            that need to be considered in developing a programme: <br>
            1 Scope of the campaign: Brands can be diluted if they are applied 
            to widely. Moreover, resources may not be available to address all 
            products and services in the country and to do so domestically and 
            internationally. The scope of a national branding programme should 
            be closely linked to the priority areas for economic development 
            growth. Key questions here include: ¥ Domestic or export focus? <br>
            </font><font face="Marlett" size="1">h</font><font face="Verdana" size="1"> 
            Which products and services should be targeted? <br>
            </font><font face="Marlett" size="1">h</font><font face="Verdana" size="1"> 
            Which sectors? <br>
            </font><font face="Marlett" size="1">h</font><font face="Verdana" size="1"> 
            Are we focusing on consumers or &quot;B2B/government&quot;? <br>
            <br>
            2 Stakeholder involvement: Key stakeholders include government, 
            industry, labour and community. Each of these stakeholders has 
            different needs and objectives with regards to promoting a national 
            brand, and each should be consulted and managed to develop the most 
            appropriate and effective programme. <br>
            <br>
            3 Brand positioning: Key brand values must be clearly understood. 
            What is the image of Omani products we want to promote Ð Modern? 
            Innovative? Top-quality? Reliable? The brand should also be 
            positioned in such a way that it can be leveraged both domestically 
            and internationally, as well as across a range of industry sectors. 
            Finally, it must be WTO-compliant: the blunt but sometimes effective 
            &quot;Buy Oman&quot; just sounds a bit too protectionist these days. <br>
            <br>
            4 Brand maintenance: This is the most important and most often 
            overlooked element of national branding. It is critical to put in 
            place exacting criteria for organisations associated with the brand. 
            Not just any company should be able to use the brand to promote 
            their goods and services Ð they should meet strict requirements for 
            quality and should have progressive programmes in place for such key 
            strategic issues such as Omanisation and environmental management. 
            Finally, brand protection must be ensured not only through the 
            initial application process but also by implementing a strict 
            programme of compliance and audit. <br>
            <br>
            5 Operational Support: Developing and managing a national branding 
            programme requires skills and resources. An effective and efficient 
            organisation must be developed to operate and support the programme.<br>
            <br>
            6 Financing: Regardless of the initial budget that can be made 
            available by government and/or industry, sustainability of national 
            branding programmes relies on stakeholder contribution to financing. 
            Paid participation leads to active involvement. <br>
            <br>
            7 Coordination: The brand for promoting Omani-made products is 
            unlikely to be the only &quot;national brand&quot; initiative in Oman. 
            Additional brands (or an umbrella brand) may well be developed for 
            promoting tourism and/or foreign investment, as well for specific 
            sectoral development initiatives (e.g., the IT sector). Effective 
            coordination and integration of these national brands is critical to 
            the success of the initiatives. <br>
            <br>
            Oman has many reasons to be proud. The goods and services that its 
            people produce should be one of them. A well-developed and executed 
            national branding programme can do a lot more than simply harness 
            latent feelings of national pride, but more importantly, it can play 
            a major role in supporting job creation and continued economic 
            development in Oman. The author is Managing Director (GCC) for 
            Kaiser Associates (www.kaiseredp.com), a global strategy and 
            economic development consulting firm based in Dubai.</font><hr color="#CC3333">
