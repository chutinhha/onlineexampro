            <p><font size="4" color="#FF0000" face="Verdana">Taking action</font><font face="Verdana"><br>
            <br>
            </font><font face="Verdana" size="1"><b>Oman's stock market is 
            considered to be the most transparent bourse in the GCC. Confidence 
            was further boosted in September when the MSM acted to safeguard 
            investors against insider trading, writes Nayl D'Souza</b></font></p>
            <p><font face="Verdana" size="1">
            <img border="0" src="msmnbo2.jpg" align="left" hspace="8" width="250" height="170">The Muscat Securities Market (MSM) 
            is a relatively small stock market by international standards. Its 
            market capitalisation of around $4.2 billion (over RO1.6 billion) 
            represents only 1.3 per cent of the total market capitalisation of 
            GCC bourses. Oman's valuations are still at the lower end of the 
            spectrum due to the absence of visible stocks with plenty of muscle 
            like Saudi Telecom or Q-Tel for instance. &quot;This factor hinders 
            regional institutional investors considering Oman as a significant 
            country exposure,&quot; remarks a local broker. But the good news is that 
            the MSM is on the boil.<br>
            The resurgence of interest in the market has 
            been aided by ongoing privatisation efforts while foreign investors 
            are salivating at the prospect of forthcoming floatations like 
            Omantel. An equities surge has already been visible this year 
            similar to that in other bourses across the region. &quot;There has been 
            a sea change in investor attitudes with risk appetites rising and 
            investor time horizons beginning to lengthen. Those Omani stocks 
            that have been undervalued for years are attracting investors even 
            when there are tepid forecasts on earnings growth,&quot; says another 
            stockbroker. <br>
            Indeed, the MSM grew over 30 per cent in the first six 
            months of 2003 and there is every indication that this growth will 
            be sustained through to next year as investor confidence continues 
            to grow. Yet despite this trend, it is debatable whether as much 
            capital would be pumped into the MSM if it were not reputed to be 
            the most progressive and best-regulated stock markets in the region. 
            <br>
            The Capital Market Authority's (CMA) firm level of supervision on 
            matters of corporate governance to public shareholding companies 
            seems to be paying substantial dividends. The guidance, management 
            and regulation from the MSM and CMA have fostered a healthy level of 
            credibility and integrity within the market. There are several rules 
            and regulations introduced by the CMA that requires companies listed 
            on the MSM to be transparent and disclose immediately any 
            significant information that may affect the prices of the shares. So 
            it is unsurprising to see the stock exchange reach for the whistle 
            when it suspects foul play that could tarnish the reputation it has 
            worked so hard to build since the bourse began operating in 1989.<br>
            <br>
            <b>Law enforcement </b><br>
            Perhaps a good example of such supervision is the 
            unprecedented measure that the MSM took on September 17 to protect 
            investors from wrongdoers who traded shares of National Bank of Oman 
            (NBO) three days earlier. Put simply, the MSM cancelled all 
            transactions of NBO shares executed on September 14. Apparently, 
            around 230,000 NBO shares had been traded on the day in question. 
            This was a much higher trading volume for the stock than on typical 
            trading days. It transpires that mainly two brokerage companies 
            handled these transactions, another factor that caused alarm bells 
            to ring. <br>
            Ahmed bin Saleh Al Marhoon, the director general of the MSM, 
            broadly says he took action because &quot;the share price of NBO had 
            surged immediately after the beginning of trading on September 14 
            which indicated that some parties had obtained internal information 
            undisclosed to other parties.&quot; This activity contravened MSM's law 
            and the regulations and instructions issued by the authorities 
            governing the MSM. The decision to cancel was made on the basis of 
            the Capital Market Law (Article 18) which empowers the director 
            general of the MSM to cancel deals that he deems inappropriate. 
            <br>
            Abdullah Salem Al Salmi, executive vice president of the CMA, 
            confirms to OER in an exclusive interview that the MSM's decision to 
            cancel the deals was made when it became obvious that &quot;the 
            information about the strategic stake acquirement by the Suhail 
            Bahwan Group in NBO was not communicated by NBO to the MSM at the 
            right time and some people traded on the basis of knowledge of the 
            proposed acquisition from an insider source.&quot; NBO disclosed news 
            about such a possible equity deal on September 16, two days after 
            the alleged insider trading took place. <br>
            <br>
            <b>Gauging reactions</b> <br>
            My guess 
            is that the impact of such a move will bolster confidence, 
            especially among foreign investors. After all, inside trading poses 
            a great problem even in developed markets. Although some 
            shareholders have expressed their unhappiness with the decision, the 
            majority of shareholders seem to be content to see action taken to 
            prevent confidential information about a company's affairs used by a 
            select few to make a profit or prevent a loss. <br>
            Al Salmi agrees, 
            saying, &quot;We cannot always find consensus on any action. Some, 
            especially those who were not allowed to gain from leaked 
            information, will make some noise. But in my view the majority have 
            appreciated such decisions. To be frank with you, the overall 
            reaction was very positive and the business community in general 
            appreciated the action. I think it sent the right message to 
            investors and listed companies.&quot;<br>
            &quot;The interest of the investors is 
            of utmost importance to the regulator and the exchange. In this 
            respect the availability of information that may affect the prices 
            of any share should be made public to the MSM, the CMA, the media 
            and the general public. Listed companies have a duty to ensure that 
            significant information must not be leaked but rather made available 
            to all. If listed companies cooperate and implement the CMA's 
            requirements, deals based on insider information will then be 
            minimised and equality of data distribution is maintained,&quot; he adds. 
            <br>
            <br>
            <b>You can't please everyone </b><br>
            Was it right to punish the guilty along 
            with law abiding investors? A shareholder who had his NBO share 
            transactions cancelled on September 14 believes the answer is no. 
            &quot;What safeguards do investors have that future share transactions 
            will not be cancelled?&quot; he asks. &quot;It would have been better to 
            specifically target the insider traders than to cancel all the share 
            transactions. No decision should cause a major disruption to the 
            stock market. Markets are supposed to operate smoothly.&quot; <br>
            One broker 
            is equally uncertain if a proper decision was taken. &quot;Time is very 
            crucial in a market. If the MSM is to take such a decision, it 
            should do so in a time-bound manner. The lack of immediate action 
            meant that brokerage companies had to deal with the aftermath of the 
            cancelled trades.&quot; <br>
            However, the bottom line is that insider trading 
            is a serious crime that affects the credibility of the market and 
            its regulator so any successful detection of it and action directed 
            against it ought to be commended so long as investor confidence is 
            not shaken.<br>
            Insider trading is difficult to detect and to prove. It 
            is also be difficult to determine who are the insider traders within 
            a short time span. The MSM would argue that it certainly did its 
            best under the circumstances to take the best course of action after 
            extensive evaluation of the issue and the circumstances. <br>
            But what if 
            the MSM had been aware of the insider dealing after T+3 (shares are 
            transacted and funds transferred 3 days after their acquisition), 
            would it have reacted differently? <br>
            &quot;Such trades if discovered after 
            T+3 would have attracted penalty under the Capital Market Law which 
            has strict penalties for trading on the basis of insider 
            information,&quot; reveals Al Salmi who reiterates that any incidents of 
            insider trading in the future will be treated on a case by case and 
            that any action from the authorities will only be in the best 
            interest of investors.<br>
            &quot;If significant information was leaked and it 
            became obvious that some tried to gain from such leaked information 
            the deal would be cancelled,&quot; explains Al Salmi. &quot;We need to see 
            more effort from listed companies to protect price sensitive 
            information from being leaked and to disclose any such information 
            immediately to the MSM, the press and the public if the company 
            feels that the information cannot be kept within the boardroom.&quot;<br>
            Doing this will of course ensure equality of information 
            distribution. By making important information available to everyone 
            at the same time a conducive investment environment can be achieved. 
            <br>
            <br>
            <b>Taking stock</b> <br>
            &quot;Regarding the case of NBO, the matter will be 
            investigated further and the cancellation of deals does not mean 
            that the case is closed,&quot; concludes Al Salmi. However, because all 
            transactions of NBO shares on September 14 were cancelled by the MSM 
            and effectively no trading ever took place one can suppose that this 
            episode has, to a large extent, come to a natural close. <br>
            Besides, NBO guarantees that its Board of Directors acted in a proper manner 
            and is adamant that disclosure of a possible deal with the Suhail 
            Bahwan Group (SBG) happened prematurely. After all, rumours had been 
            circulating for many days before September 14 about the 
            possibilities of such a pact. <br>
            At a meeting on October 21 where 
            shareholders unanimously approved an equity private placement to the 
            tune of RO53.2 million to the Suhail Bahwan Group, a senior NBO 
            spokesman told OER a related party purchase on September 16 by a 
            Ministry Pension Fund was flagged up and triggered the CMA to 
            request a statement from the bank forcing it to disclose information 
            about the proposed deal with Suhail Bahwan Group that both parties 
            had agreed in principal on September 4. <br>
            &quot;We would have much 
            preferred to have disclosed information at a later date when the 
            deal was closed. There was lots of negotiating on terms and 
            conditions still to be done,&quot; says the spokesman who confirms that a 
            final agreement between NBO and SBG was reached as late as September 
            23. &quot;It is important to also emphasise that NBO has very strict 
            confidentiality internal controls and directors are constantly 
            reminded about close periods of share trading.&quot; <br>
            <br>
            <b>Banking on a 
            turnaround </b><br>
            The terms of the transaction, which has already secured 
            all requisite regulatory approvals, entails the placement of over 24 
            million shares at a price of RO2.200 per share to the Suhail Bahwan 
            Group. The proceeds of the issue will be used to strengthen the 
            bank's balance sheet and loan coverage capacity in order to deliver 
            sustained future profitability.<br>
            The deal couldn't have come at a 
            better time for NBO. The bank's profitability has been under severe 
            pressure since 2000 on mounting non-performing loans and the 
            subsequent provisions. No wonder then that NBO's share price 
            increased by RO0.950 from the time shareholders knew about the 
            proposed deal to the close of trade on the day they approved it. But 
            even on this day, the bank released a grizzly forecast that high 
            levels of provisions in order to complete the exercise of 'cleaning 
            up' the loan portfolio will further dent the P&amp;L account. Officials 
            envisage that the bank will report an operating loss for 2003 in the 
            region of RO52 million. <br>
            &quot;NBO is at its lowest ebb but we have 
            already begun to turn things around,&quot; says John P. Finigan, chief 
            executive officer of NBO. &quot;The combination of the necessary but 
            painful measures to be taken this year will leave the bank well 
            placed to generate sustained levels of operating profit from the 
            first quarter of 2004 as well as presenting the firm expectation of 
            a restoration of dividend to our shareholders for the years 2004 and 
            onwards.&quot;</font></p>
