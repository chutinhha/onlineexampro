                    <b>Life's journey </b><br>
                    A 'lucky' number 13 from a family of 20 children, Abdulla 
                    was born in Zanzibar. He has fond memories of a happy 
                    childhood. Both of his parents made sure that schooling and 
                    further education was central to the upbringing of their 
                    children, most of whom subsequently went on to obtain 
                    university degrees. From the 20 children, five became 
                    doctors, two economists, one a lawyer, one a teacher, one a 
                    chemist, one a pharmacist, one a nurse and then, of course, 
                    there is Abdulla, the nuclear physicist who turned into a 
                    petroleum engineer. <br>
                    Abdulla's family moved to Egypt in 1965 and then to Oman in 
                    1971. During this time, Abdulla admits he was fortunate to 
