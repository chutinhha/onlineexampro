                    <img border="0" src="profile.jpg" align="left" hspace="5" vspace="5" width="150" height="190">Dr 
                    Abdulla Lamki, deputy managing director, Petroleum 
                    Development Oman (PDO), has been lured away from the 
                    prospect of a deserved retirement. As the oil behemoth 
                    strives to discover and recover more oil, it's good to 
                    retain professionals that can provide stable leadership and 
                    unwavering support to the company's management. As it 
                    happens, Abdulla's friends and family are also very 
                    fortunate to keep him because ten years ago his life was 
                    nearly taken away. <br>
                    The experience deeply unnerved him. Abdullah reveals that 
                    the biggest challenge of his life was managing the crisis of 
                    confidence he had upon suffering a brain haemorrhage in 
                    1993, a few weeks before he turned 50. &quot;Initially, the pain 
                    felt like a bomb exploding in head. I can really empathise 
                    with those who have suffered a trauma,&quot; he says in an 
                    exclusive interview with OER. <br>
                    Following medical treatment in the UK, Abdulla returned to 
                    Oman but he found to his complete dismay that his 
                    decision-making processes had significantly slowed down. &quot;I 
                    was not myself for over 3 months. As you can imagine, I 
                    became disorientated and it was a real challenge to regain 
                    my confidence,&quot; he recalls. &quot;I was determined to pick myself 
                    up. I remember going to an industry conference in Jakarta. 
                    It was a tough decision to go but I did not falter. I had to 
                    push myself to do it and to regain the self-belief that I 
                    had lost.&quot;<br>
                    <br>
                    <b>Digging deep to succeed </b><br>
                    Abdulla certainly has not been a stranger to meeting 
                    challenges. Since 1970, enormous changes and extreme tests 
                    have engulfed the local oil and gas industry in the wake of 
                    rapid expansion and development. Very early on during his 
                    career at PDO, he started to challenge many of the 
                    unquestioned assumptions related to the company's procedures 
                    and has thereby forced fundamental progress. <br>
                    Abdulla explains that the biggest turning point in his life 
                    came when he was teaching Physics for two years at the 
                    University of Baghdad, saying, &quot;I had an option to continue 
                    to teach in Iraq but I decided to return to Oman in 1973. I 
                    did not have it in my mind to become an oilman but I joined 
                    the oil industry here and I have never looked back since.&quot;<br>
                    During Abdulla's 30-year tenure at PDO, the company has 
                    soared to become the second largest employer of Omani 
                    nationals (after the government) in the country. It employed 
                    3,470 Omani employees at the end of 2001 (an Omanisation 
                    level of around 82 per cent) and it relied on a contractor 
                    workforce of over 10,000 employees. Moreover, as a boost to 
                    local businesses, PDO purchases over US$200 million of 
                    services and materials from local suppliers and contractors 
                    alone. <br>
                    Even his harshest critics would admit that Abdulla's 
                    management has had crucial and indispensable human 
                    dimensions. He devotes a time to getting to know his 
                    associates - not only the chief executives but also less 
                    highly placed individuals from all spheres of the company 
                    activities. A tireless worker and a devout family man, 
                    Abdulla is constantly in touch with reality and a person who 
                    promotes a strong work ethic. He reflects that his mother, 
                    who had no formal education, was an extremely wise woman 
                    whose advice has helped him build a successful career. &quot;One 
                    of the most memorable sayings my mother taught me was that 
                    people must earn rewards, no one is entitled to them.&quot;<br>
