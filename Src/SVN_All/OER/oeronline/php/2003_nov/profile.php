                    gained a doctorate in Physics several years later. <br>
                    &quot;Probably the proudest moment in my life was when I had been 
                    elected a Foundation Scholar at Trinity College in Dublin,&quot; 
                    he reflected. &quot;I was 20 years old at the time but I remember 
                    distinctly the moment when my name was called out in front 
                    of the town's square to announce my election. Over 200 
                    students had tried to clinch it.&quot; <br>
