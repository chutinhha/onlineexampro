                    institute. He then took a lecture post at the University of 
                    Baghdad teaching Physics and Electronics. Two years later, 
                    in 1973, he moved to Muscat to join his parents and started 
                    work with PDO. Thirty years on, Abdulla is a reluctant hero. 
