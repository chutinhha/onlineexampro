            the country's first experiment with democracy. Voted into office in 
            an unprecedented direct ballot, they stand apart from their 
            predecessors as a body that is a genuinely representative 
            institution of the Omani people. Indeed, last month's nationwide 
            elections on October 4, widely acknowledged as transparent, fair and 
            trouble-free, enabled registered Omani voters to exercise their 
            right to elect 83 members from 59 wilayats to the fifth term of the 
            Majlis Al Shura. As expected, the turnout was moderate in urban 
            areas, but enthusiastic in the rural heartland of the Sultanate. <br>
            A final list of the winners (released a week later after due 
            consideration of feedback from the preliminary results) sealed the 
            victory of 81 men and two women, the large majority entering office 
            for the first time. There's little doubt that the nation has emerged 
            significantly invigorated by these elections. The electoral reforms 
            unveiled by the Interior Minister, Sayyid Saud bin Ibrahim Al 
            Busaidi, were path-breaking in the context of the government's 
            efforts to foster the growth of a mature representative council in 
            the Sultanate. The measures attracted wide praise in the local and 
            regional Arab media as testimony of Oman's continuing commitment to 
            advancing the democratic ideals of participatory politics. <br>
            Accordingly, all Omani citizens of 21 years of age and above were 
            entitled to vote in the elections, in a far cry from the drastically 
            small number of voters who exercised their franchise in previous 
            ballots. Thus, of a total population of nearly 2 million Omanis, 
            800,000 were eligible to vote. As it happens, there was some voter 
            apathy with only 262,000 signing up to obtain voting cards but this 
            was over double the number who registered to vote at the last Majlis 
            Al Shura elections in 2000. <br>
            Also for the first time in the history of the Majlis Al Shura 
            elections, Omanis residing in the UAE, Bahrain, Qatar, Egypt and 
            Jordan were given the opportunity to choose their own 
            representatives. Omanis in the UAE were the first to cast their vote 
            well ahead of polls at home, while those residing in Bahrain, Egypt 
            and Jordan exercised their franchise a few days later. <br>
            In recognition of their increasingly important role in society and 
            government, women were accorded equal rights with men as candidates 
            for the elections. Limits on the number of women voting or 
            contesting as candidates in future elections were lifted as well. 
            They were able to campaign and canvass votes as their male 
            counterparts did. Although women, 15 of whom offered their 
            candidature for the polls, fared worse than was forecast (pundits 
            predicted four seats at the Majlis Al Shura would go to women) far 
            more is today being done to foster leadership and initiative among 
            Oman's fairer sex. <br>
            Significant administrative, procedural and legal changes were also 
            made to the fifth Majlis Al Shura election rules in order to ensure 
            a simplified and streamlined ballot process. By simplifying the 
            voting process, the government sought to ensure the greatest 
            possible participation of the electorate in the next elections. <br>
            A new electoral register was established at the Interior Ministry's 
            General Diwan that computed information on voters and their 
            respective constituencies into a new database. The election 
            authorities used an effective combination of Information Technology, 
            the Internet, and user-friendly voter registration procedures to 
            accomplish voter registration.<br>
            Keen to ensure a free and fair polling process, the government also 
            set up a committee consisting of the Justice Ministry 
            Under-Secretary and two deputies of the President of the Supreme 
            Court to closely supervise the elections. For its part, the 
            government pledged to maintain strict neutrality in the conduct of 
            the poll, and to abide by the poll result. Candidates employed by 
            government were asked to sever their direct links with the state as 
            a pre-condition for contesting the elections. <br>
            Moreover, aggrieved candidates disputing a poll result had ample 
            avenues to seek redress, initially by lodging a complaint with the 
            local election committee in the wilayat concerned. However, 
            re-elections of any kind were strictly ruled out. <br>
