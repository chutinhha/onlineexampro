            duly empowered Majlis body imbued with a strong sense of national 
            purpose as it begins a new three-year term. As enunciated by 
            Interior Minister Sayyid Saud, this democratically elected body of 
            representatives will aim to fulfil the needs and aspirations of the 
            people of their wilayats.<br>
            <br>
            <b>Historical roots </b><br>
            The reforms have strengthened the Sultanate's reputation as one of 
            the most progressive of the Gulf states in the realm of 
            participatory politics. The foundations for the Majlis Al Shura was 
            laid by His Majesty Sultan Qaboos more than two decades ago in line 
            with His Majesty's fervent desire to give the Omani people a voice 
            in national affairs and thereby enlist the population in the 
            collective goal of building the nation. <br>
            In 1981, His Majesty established the State Consultative Council, 
            which was essentially a nominated body. A decade later, this evolved 
            into the Majlis Al Shura whose members are all elected with the 
            exception of the President, who is appointed by Royal Decree. The 
            first Majlis Al Shura was inaugurated in 1991 with 59 members 
            representing each wilayat of the Sultanate. <br>
            With a view to strengthening the consultative process in the 
            Sultanate, His Majesty established the Majlis Al Dawla (State 
            Council) whose formation was decreed in the Basic Statute of the 
            State, a landmark piece of legislation enacted by His Majesty to 
            ensure durable political and social stability. The Majlis Al Dawla, 
            along with the Majlis Al Shura, became the two chambers of the 
            Majlis Oman (Council of Oman). History was made when the Majlis 
            Addawla held its first meeting in January 1998 after being 
            inaugurated by His Majesty. It offers judicious advice to His 
            Majesty and works closely with the Majlis Al Shura.<br>
            In its role as an upper chamber, the Majlis Al Dawla's central focus 
            is on developmental issues. The Council's President and members are 
            all distinguished members of the community drawn from all walks of 
            life. Their expertise and considered opinion is key to the Council's 
            deliberations on vital issues of national developmental. <br>
            Appointed for a three-year term by Royal Decree, members must be 
            Omani nationals aged 40 years and above. They can be drawn from 
            among former ministers, under-secretaries and those of equivalent 
            rank, former ambassadors and senior judges, and civilians who have 
            distinguished themselves in science, culture and the arts. Senior 
            faculty staff of universities and colleges, as well as dignitaries 
            and accomplished businessmen, are also potential candidates for 
            nomination. <br>
            Significantly, five women are among the 53 members of the current 
            Majlis Al Dawla, underlining His Majesty's commitment to enhancing 
            the role of women in all key areas of public life.</font></p>
