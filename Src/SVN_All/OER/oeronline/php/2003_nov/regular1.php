            negative given the improving economic outlook, but on the margin, 
            when you have higher oil prices, it could crimp consumer spending,&quot; 
            said Rich Nash, chief market strategist at Victory Capital 
            Management in Cleveland, US. <br>
            In truth, OPEC is unlikely to do anything that risks upsetting the 
            global economic recovery. OPEC ministers understand well the 
            economics of the oil market - they know that global economic 
            slowdown translates into weak oil demand, lower production and lower 
            prices. Their move in late September was more defensive than 
            offensive, and if prices threaten to breach $30/b, OPEC will likely 
            turn the taps back on to cool the market.<br>
