            disdain. As the West's energy watchdog, the IAE reacts badly to any 
            move that threatens to inflate prices and, by extension, threaten 
            economic growth in the industrialised world. &quot;Given the continued 
            weakness of the world economy, OPEC's strategy to maintain prices at 
            persistently high levels cannot contribute to a sustained recovery 
            and a return to global economic growth,&quot; said Claude Mandil, head of 
            the Paris-based agency. <br>
