            trick. Oil prices have been above $30 per barrel almost ever since 
            the cartel cut output in late September. And UAE Oil Minister Obaid 
            bin Saif al-Nasseri has warned that a U-turn is unlikely in the 
            foreseeable future - in late October, he said world oil markets 
            would be more than adequately supplied through the first quarter of 
            2004, and warned that stockpiles are building. <br>
            &quot;The powers of supply and demand indicate that there are adequate 
            oil supplies in the last quarter of this year and the first quarter 
            of next year, which will lead to a buildup in global oil stockpiles 
            during this period of high demand. Crude oil stockpiles in the main 
            consuming countries are increasing and are now higher than last 
            year,&quot; he told a fuel bunkering conference in the UAE's emirate of 
            Fujairah. <br>
            The current price spike dates back to the unexpected events of late 
            September. On Tuesday, September 23, it seemed that OPEC was poised 
            to keep its production quotas unchanged. OPEC oil ministers were 
            gathering in Vienna for one of their regular meetings to discuss the 
            cartel's closely watched quotas, and high profile figures were 
            stressing the need to a period of stability. <br>
            &quot;We have to study the situation and take the necessary step,&quot; said 
            Obeid bin Saif Al Nasiri, UAE Minister of Petroleum and Mineral 
            Resources, upon his arrival in Vienna. &quot;There is no big reason or 
            strong reasons for change.&quot; <br>
            The UAE was not the only major producer pledging to weather a 
            downturn in crude prices by keeping output limits unchanged for the 
            rest of the year: Al Nasiri's message was echoed by his Kuwaiti 
            counterpart, Sheikh Ahmad Fahd Al Ahmad Al Sabah. &quot;We are very 
            worried about the prices, especially these days, with prices not 
            very good,&quot; he told reporters on the eve of the meeting. &quot;But I 
            think we have to continue with the same production we have now and 
            continue to watch the market. If it is necessary to act, we have to 
            act as soon as prices are above or below the target range.&quot; Sheikh 
            Ahmad said he thought a meeting in December would be a &quot;good 
            choice.&quot; <br>
            What a difference a day makes. Little more than 24 hours after 
            Kuwait and the UAE - heavyweight producers within the OPEC hierarchy 
            - had been pledging to keep quotas unchanged, the cartel slashed output 
            by some 4 per cent, to 24.5 million barrels per day (b/d). Prices 
            had fallen throughout September to below $25 per barrel (/b), OPEC's 
            target price for oil. Producers felt they had to act to arrest the 
            decline before it gathered too much momentum <br>
            &quot;Having reviewed the current oil market, the [OPEC] Conference noted 
            that, whilst the global economy appears to be improving, only 
            normal, seasonal growth in demand is expected for the fourth 
            quarter, and the market continues to be well-supplied,&quot; said an OPEC 
            communiqué after the meeting on September 24. <br>
            &quot;In view of the continued rise in non-OPEC supplies and the ongoing 
            recovery in Iraqi production, stocks have been replenished and are 
            rapidly reaching normal seasonal levels, with the supply/demand 
            balance for the fourth quarter 2003 and first quarter 2004 
            indicating a contra-seasonal stock build-up. This could have a 
            destabilising effect on the market which requires a reduction of 
            supplies from all producers to ensure stability.&quot; The forecast 
            gradual return of Iraqi crude to the market was a further factor 
            weighing on OPEC's mind. <br>
            The move shocked oil traders, who had been banking on a 'no change' 
            policy. On London's International Petroleum Exchange, November Brent 
            jumped more than 4 per cent, to $26.85/b. Many dealers lost money on 
            the unexpected price swing. &quot;The hedge funds are short and they will 
            be running for cover,&quot; said Gary Ross of New York consultancy PIRA 
            Energy - a reference to the fact that many hedge fund managers had 
            bet on prices falling further. <br>
